package sysman.util.consumo.pojo;

public class RespuestaHistoricosAud {

	private String Usuario;
	private String Fecha;
	private String Dir_Ip;
	private String Nombre_Proceso;
	private String Accion;
	private String Valor_Inicial;
	private String Valor_Final;
	/**
	 * @return the usuario
	 */
	public String getUsuario() {
		return Usuario;
	}
	/**
	 * @param usuario the usuario to set
	 */
	public void setUsuario(String usuario) {
		Usuario = usuario;
	}
	/**
	 * @return the fecha
	 */
	public String getFecha() {
		return Fecha;
	}
	/**
	 * @param fecha the fecha to set
	 */
	public void setFecha(String fecha) {
		Fecha = fecha;
	}
	/**
	 * @return the dir_Ip
	 */
	public String getDir_Ip() {
		return Dir_Ip;
	}
	/**
	 * @param dir_Ip the dir_Ip to set
	 */
	public void setDir_Ip(String dir_Ip) {
		Dir_Ip = dir_Ip;
	}
	/**
	 * @return the nombre_Proceso
	 */
	public String getNombre_Proceso() {
		return Nombre_Proceso;
	}
	/**
	 * @param nombre_Proceso the nombre_Proceso to set
	 */
	public void setNombre_Proceso(String nombre_Proceso) {
		Nombre_Proceso = nombre_Proceso;
	}
	/**
	 * @return the accion
	 */
	public String getAccion() {
		return Accion;
	}
	/**
	 * @param accion the accion to set
	 */
	public void setAccion(String accion) {
		Accion = accion;
	}
	/**
	 * @return the valor_Inicial
	 */
	public String getValor_Inicial() {
		return Valor_Inicial;
	}
	/**
	 * @param valor_Inicial the valor_Inicial to set
	 */
	public void setValor_Inicial(String valor_Inicial) {
		Valor_Inicial = valor_Inicial;
	}
	/**
	 * @return the valor_Final
	 */
	public String getValor_Final() {
		return Valor_Final;
	}
	/**
	 * @param valor_Final the valor_Final to set
	 */
	public void setValor_Final(String valor_Final) {
		Valor_Final = valor_Final;
	}
}
