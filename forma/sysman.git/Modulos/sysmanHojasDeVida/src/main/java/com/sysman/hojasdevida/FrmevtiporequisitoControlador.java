    public boolean insertarAntes() {
        // <CODIGO_DESARROLLADO>
        try {
            Long consec = ejbSysmanUtil.generarSiguienteConsecutivo(
                            "EV_TIPO_REQUISITO",
                            "1 = 1",
                            "CODIGO");
            String consecutivo = SysmanFunciones
                            .padl(consec.toString(), 5, "0");
            registro.getCampos().put(cCodigo, consecutivo);
            registro.getCampos().put(FrmevtiporequisitoControladorEnum.COMPANIA
                            .getValue(),
                            compania);
        }
        catch (SystemException e) {
            logger.error(e.getMessage(), e);
            JsfUtil.agregarMensajeError(e.getMessage());
        }
        // </CODIGO_DESARROLLADO>
        return true;
    }

