  CREATE OR REPLACE EDITIONABLE TRIGGER "#ESQUELETO_UNIST#"."AIU_ANO_ACME" FOR INSERT OR UPDATE OF   COMPANIA, NUMERO  ON ANO 
/*
Release 2.0.0
*/
COMPOUND TRIGGER
  MI_COMPANIA VARCHAR2(32000);
  MI_NUMERO   NUMBER;
  MI_EXISTE   NUMBER:=0;
  MI_CAMPOS   VARCHAR2(32000);
  MI_VALORES  VARCHAR2(32000);
  MI_RTA      NUMBER;  
  -- ES COMPUESTO POR QUE SE CONSULTA LA TABLA Y SE CREA UNA TABLA MUTANTE
-- CON ESTE TIPO GUARDO LOS REGISTROS POR CADA REGISTRO DEL INSERT O UPDATE PARA LUEGO PROCESARLO
  TYPE REGISTRO IS RECORD 
  (
  COMPANIA                  ANO.COMPANIA%TYPE,
	NUMERO                    ANO.NUMERO%TYPE,
  CREATED_BY                ANO.CREATED_BY%TYPE
	);  
  TYPE REGISTROS IS TABLE OF REGISTRO INDEX BY BINARY_INTEGER ;
  TABLA REGISTROS;
  MI_POS NUMBER DEFAULT 0;
  RTA    NUMBER;
AFTER EACH ROW IS
BEGIN
  --POR CADA FILA SE CARGA LA TABLA PARA LUEGO LARZAR EL REGISTRO
  MI_POS := MI_POS+1;
  TABLA(MI_POS).COMPANIA       := :NEW.COMPANIA;
  TABLA(MI_POS).NUMERO         := :NEW.NUMERO;
  IF UPDATING THEN
    TABLA(MI_POS).CREATED_BY     := :NEW.MODIFIED_BY;  
  ELSE
    TABLA(MI_POS).CREATED_BY     := :NEW.CREATED_BY;  
  END IF;
END AFTER EACH ROW;
AFTER STATEMENT IS
BEGIN
  FOR i IN 1..MI_POS LOOP
    RTA:= PCK_SYSMAN_UTL.FC_INSERTARMESESDELANO(UN_COMPANIA => TABLA(MI_POS).COMPANIA,
                                                UN_ANIO     => TABLA(MI_POS).NUMERO,
                                                UN_CREATED_BY =>TABLA(MI_POS).CREATED_BY);   
  END LOOP;  
END AFTER STATEMENT;
END AIU_ANO_ACME ;
/
ALTER TRIGGER "#ESQUELETO_UNIST#"."AIU_ANO_ACME" ENABLE;