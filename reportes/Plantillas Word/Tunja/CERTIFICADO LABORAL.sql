SELECT 
TO_CHAR(SYSDATE,'DD "de" Month" de" YYYY','nls_date_language=spanish') "FECHA ACTUAL",   
PERSONAL.ID_DE_EMPLEADO,
PERSONAL.NOMBRECOMPLETO,
TO_CHAR(PERSONAL.NUMERO_DCTO,'99G999G999G999','NLS_NUMERIC_CHARACTERS = ".,"') AS CEDULA,
CD1.NOMBRE AS EXPEDIDA,
TO_CHAR(PERSONAL.FECHA_DE_INGRESO,'DD "de" Month"del" YYYY','nls_date_language=spanish') "FECHA INGRESO",
PERSONAL.ID_DE_CARGO,
C.NOMBRE_DEL_CARGO AS CARGO,
TO_CHAR(CT.SALARIO_BASE,'99G999G999G999','NLS_NUMERIC_CHARACTERS = ".,"') AS SALARIOBASE,
PCK_SYSMAN_UTL.FC_VALOR_LETRAS(CT.SALARIO_BASE,0) AS VALORLETRASSB,
personal.cargo_ENCARGO,
case  when personal.cargo_ENCARGO IS null
then
''
else
'Que actualmente cumple funciones de encargo en el empleo ' ||
C1.NOMBRE_DEL_CARGO ||' devengando un salario mensual de '
||PCK_SYSMAN_UTL.FC_VALOR_LETRAS(CT1.SALARIO_BASE,0) ||'($' ||
TO_CHAR(CT1.SALARIO_BASE,'99G999G999G999','NLS_NUMERIC_CHARACTERS = ".,"') ||')' end AS ENCARGO,
CD.NOMBRE AS CIUDAD,
AUS.CONSECUTIVO,
FM.NOMBREFORMA AS FORMA_NOMBRAMIENTO,
PCK_SYSMAN_UTL.FC_PAR(PERSONAL.COMPANIA, 'NOMBRE DE QUIEN FIRMA CERTIFICADO DE HISTORIA LABORAL', 6, SYSDATE) FIRMA_CERTIFICADO, 
PCK_SYSMAN_UTL.FC_PAR(PERSONAL.COMPANIA, 'CARGO DE QUIEN FIRMA CERTIFICADO DE HISTORIA LAB', 6, SYSDATE) CARGO_CERTIFICADO 
FROM PERSONAL
LEFT JOIN CIUDAD CD1
 ON  PERSONAL.PAIS_CED             = CD1.PAIS
 AND PERSONAL.DEPARTAMENTO_CED     = CD1.DEPARTAMENTO
 AND PERSONAL.EXPEDIDA             = CD1.CODIGO
INNER JOIN CARGOS C
 ON  PERSONAL.COMPANIA                = C.COMPANIA
 AND PERSONAL.ESCALAFON               = C.ESCALAFON
 AND PERSONAL.ID_DE_CARGO                 = C.ID_DE_CARGO
INNER JOIN CATEGORIA CT
 ON  PERSONAL.COMPANIA            = CT.COMPANIA
 AND PERSONAL.ESCALAFON           = CT.ESCALAFON
 AND PERSONAL.ID_DE_CATEGORIA           = CT.ID_DE_CATEGORIA
 AND PERSONAL.ANO                   = CT.ANO
LEFT JOIN ENCARGOS
 ON  PERSONAL.COMPANIA                = ENCARGOS.COMPANIA
 AND PERSONAL.ID_DE_EMPLEADO          = ENCARGOS.ID_DE_EMPLEADO
 AND PERSONAL.CARGO_ENCARGO          = ENCARGOS.ID_DE_CARGO
LEFT JOIN CARGOS C1
  ON ENCARGOS.COMPANIA             = C1.COMPANIA
  AND ENCARGOS.ID_DE_CARGO         = C1.ID_DE_CARGO 
  AND ENCARGOS.ESCALAFON           = C1.ESCALAFON 
LEFT JOIN CATEGORIA CT1
 ON  ENCARGOS.COMPANIA            = CT1.COMPANIA
 AND ENCARGOS.ESCALAFON           = CT1.ESCALAFON
 AND ENCARGOS.ID_DE_CATEGORIA     = CT1.ID_DE_CATEGORIA
 AND ENCARGOS.ANO  = CT1.ANO
INNER JOIN COMPANIA CP
 ON PERSONAL.COMPANIA              = CP.CODIGO
INNER JOIN CIUDAD CD
 ON  CP.PAIS                = CD.PAIS
 AND CP.DEPARTAMENTO        = CD.DEPARTAMENTO
 AND CP.CIUDAD              = CD.CODIGO
LEFT JOIN AUT_SOLICITUDES AUS
 ON  PERSONAL.COMPANIA             = AUS.COMPANIA
 AND PERSONAL.ID_DE_EMPLEADO       = AUS.ID_DEMPLEADO
INNER JOIN FORMANOMBRAMIENTO FM
 ON  PERSONAL.COMPANIA             = FM.COMPANIA
 AND PERSONAL.DE_CARRERA           = FM.IDFORMA
WHERE 
 AUS.COMPANIA = s$compania$s
AND AUS.CONSECUTIVO         = s$consecutivo$s
AND AUS.CLASE_SOLICITUD     = s$claseSolicitud$s