SELECT 
P.NOMBRES || ' ' ||P.APELLIDO1 || ' '||  P.APELLIDO2 NOM_EMPLEADO,
P.NUMERO_DCTO NUM_DOC,
REPLACE(C.NOMBRE_DEL_CARGO,TRIM(REGEXP_REPLACE(C.NOMBRE_DEL_CARGO,'[A-Za-z]')),'') NOM_CARGO,
REPLACE (CA.CODIGOGRADO, '-',' Grado ') GRADO,
CASE WHEN P.SEXO = 'F' THEN 'la doctora' ELSE 'el doctor' END GEN
FROM
PERSONAL P
LEFT JOIN CARGOS C
ON C.COMPANIA = P.COMPANIA
AND C.ESCALAFON = P.ESCALAFON
AND C.ID_DE_CARGO = P.ID_DE_CARGO
LEFT JOIN CATEGORIA CA
ON P.COMPANIA = CA.COMPANIA
AND P.ESCALAFON = CA.ESCALAFON
AND P.ID_DE_CATEGORIA  = CA.ID_DE_CATEGORIA
AND P.ANO = CA.ANO
WHERE
P.COMPANIA = s$compania$s
AND P.ID_DE_EMPLEADO = s$idEmpleado$s