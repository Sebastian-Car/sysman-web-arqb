SELECT DISTINCT
P.NOMBRES || ' ' ||P.APELLIDO1 || ' '||  P.APELLIDO2 NOM_EMPLEADO,
P.NUMERO_DCTO NUM_DOC,
REPLACE(C.NOMBRE_DEL_CARGO,TRIM(REGEXP_REPLACE(C.NOMBRE_DEL_CARGO,'[A-Za-z]')),'') NOM_CARGO,
REPLACE (CA.CODIGOGRADO, '-',', Grado ') GRADO,
SUBSTR(V.FECHA_INICIO,1,2) ||' de '|| 
PCK_SYSMAN_UTL.FC_NOMBRE_MES(SUBSTR(V.FECHA_INICIO,4,2)) ||' de '||
SUBSTR(V.FECHA_INICIO,7,4) FECHA_INICIO,
SUBSTR(V.FECHA_FINAL,1,2) ||' de '||
PCK_SYSMAN_UTL.FC_NOMBRE_MES(SUBSTR(V.FECHA_FINAL,4,2)) ||' de '||
SUBSTR(V.FECHA_FINAL,7,4) FECHA_FINAL,
SUBSTR(V.INICIO_DISFRUTE,1,2) ||' de '||
PCK_SYSMAN_UTL.FC_NOMBRE_MES(SUBSTR(V.INICIO_DISFRUTE,4,2)) ||' de '||
SUBSTR(V.INICIO_DISFRUTE,7,4) INICIO_DISFRUTE,
SUBSTR(V.FINAL_DISFRUTE,1,2) ||' de '||
PCK_SYSMAN_UTL.FC_NOMBRE_MES(SUBSTR(V.FINAL_DISFRUTE,4,2)) ||' de '||
SUBSTR(V.FINAL_DISFRUTE,7,4) FINAL_DISFRUTE,
CASE WHEN V.DIAS = 0 THEN V.DIASDINERO ELSE V.DIAS END DIAS,
V.DIASHABILES,
V.ACTO,
SUBSTR(V.FECHA_ACTO,1,2) || ' de ' ||
PCK_SYSMAN_UTL.FC_NOMBRE_MES(SUBSTR(V.FECHA_ACTO,4,2)) ||' de '||
SUBSTR(V.FECHA_ACTO,7,4) FECHA_ACTO,
SUM(CASE WHEN H.ID_DE_CONCEPTO IN ('175','174') THEN  H.VALOR ELSE 0 END) IND_VAC,
SUM(CASE WHEN H.ID_DE_CONCEPTO = ('155') THEN H.VALOR ELSE 0 END) PRIM_VAC,
SUM(CASE WHEN H.ID_DE_CONCEPTO = ('151') THEN H.VALOR ELSE 0 END) BON_REC
FROM HISTORICOS H
LEFT JOIN PERSONAL P
ON P.COMPANIA = H.COMPANIA
AND P.ID_DE_EMPLEADO = H.ID_DE_EMPLEADO
LEFT JOIN CARGOS C
ON C.COMPANIA = P.COMPANIA
AND C.ESCALAFON = P.ESCALAFON
AND C.ID_DE_CARGO = P.ID_DE_CARGO
LEFT JOIN CATEGORIA CA
ON P.COMPANIA = CA.COMPANIA
AND P.ESCALAFON = CA.ESCALAFON
AND P.ID_DE_CATEGORIA  = CA.ID_DE_CATEGORIA
AND P.ANO = CA.ANO
LEFT JOIN VACACIONES V
ON V.COMPANIA = P.COMPANIA
AND V.ID_DE_EMPLEADO = P.ID_DE_EMPLEADO
AND V.ANO = (SELECT MAX(V.ANO) FROM VACACIONES V WHERE  V.COMPANIA = s$compania$s AND V.ID_DE_EMPLEADO = s$idEmpleado$s)
WHERE
H.COMPANIA = s$compania$s
AND H.ID_DE_EMPLEADO = s$idEmpleado$s
AND H.MES = s$mes$s
AND H.ANO = s$ano$s
AND H.PERIODO = s$periodo$s
AND H.ID_DE_PROCESO = s$idProceso$s 
GROUP BY 
P.NOMBRES || ' ' ||P.APELLIDO1 || ' '|| P.APELLIDO2, P.NUMERO_DCTO,
REPLACE(C.NOMBRE_DEL_CARGO,
TRIM(REGEXP_REPLACE(C.NOMBRE_DEL_CARGO,'[A-Za-z]')),''),
REPLACE (CA.CODIGOGRADO, '-',', Grado '),
SUBSTR(V.FECHA_INICIO,1,2), 
PCK_SYSMAN_UTL.FC_NOMBRE_MES(SUBSTR(V.FECHA_INICIO,4,2)),
SUBSTR(V.FECHA_INICIO,7,4),
SUBSTR(V.FECHA_FINAL,1,2),
PCK_SYSMAN_UTL.FC_NOMBRE_MES(SUBSTR(V.FECHA_FINAL,4,2)),
SUBSTR(V.FECHA_FINAL,7,4), 
SUBSTR(V.INICIO_DISFRUTE,1,2),
PCK_SYSMAN_UTL.FC_NOMBRE_MES(SUBSTR(V.INICIO_DISFRUTE,4,2)),
SUBSTR(V.INICIO_DISFRUTE,7,4),
SUBSTR(V.FINAL_DISFRUTE,1,2),
PCK_SYSMAN_UTL.FC_NOMBRE_MES(SUBSTR(V.FINAL_DISFRUTE,4,2)), 
SUBSTR(V.FINAL_DISFRUTE,7,4),
CASE WHEN V.DIAS = 0 THEN V.DIASDINERO ELSE V.DIAS END,
V.ACTO,
SUBSTR(V.FECHA_ACTO,1,2),
PCK_SYSMAN_UTL.FC_NOMBRE_MES(SUBSTR(V.FECHA_ACTO,4,2)), 
SUBSTR(V.FECHA_ACTO,7,4),
V.DIASHABILES
