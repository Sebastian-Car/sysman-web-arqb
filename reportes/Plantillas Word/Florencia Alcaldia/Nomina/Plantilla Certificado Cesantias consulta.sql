SELECT 
P.NOMBRES || ' ' ||P.APELLIDO1 || ' '||  P.APELLIDO2 NOM_EMPLEADO,
P.NUMERO_DCTO NUM_DOC,
VF.NOMBRE_FONDO_CESANTIAS FONDO_CES,
SUBSTR(s$fechaReporte$s,1,2) DIA,
PCK_SYSMAN_UTL.FC_NOMBRE_MES(SUBSTR(s$fechaReporte$s,4,2))MES,
SUBSTR(s$fechaReporte$s,7,4) ANO,
CASE WHEN P.SEXO = 'F' THEN 'identificada' ELSE 'identificado' END GEN
FROM
PERSONAL P
 LEFT JOIN V_FONDO_DE_CESANTIAS VF
 ON VF.COMPANIA = P.COMPANIA
 AND VF.FONDO_CESANTIAS = P.FONDO_CESANTIAS
WHERE
P.COMPANIA = s$compania$s
AND P.ID_DE_EMPLEADO = s$idEmpleado$s