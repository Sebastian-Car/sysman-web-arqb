SELECT 
P.NOMBRES || ' ' ||P.APELLIDO1 || ' '||  P.APELLIDO2 NOM_EMPLEADO,
P.NUMERO_DCTO NUM_DOC,
REPLACE(C.NOMBRE_DEL_CARGO,TRIM(REGEXP_REPLACE(C.NOMBRE_DEL_CARGO,'[A-Za-z]')),'') NOM_CARGO,
REPLACE (CA.CODIGOGRADO, '-',' Grado ') GRADO,
SUBSTR(V.FECHA_INICIO,1,10) FECHA_INICIO,
SUBSTR(V.FECHA_FINAL,1,10) FECHA_FINAL,
SUBSTR(V.INICIO_DISFRUTE,1,2) DIA_INDIS,
PCK_SYSMAN_UTL.FC_NOMBRE_MES(SUBSTR(V.INICIO_DISFRUTE,4,2))MES_INDIS,
SUBSTR(V.INICIO_DISFRUTE,7,4) ANO_INDIS,
SUBSTR(V.FINAL_DISFRUTE,1,2) DIA_FNDIS,
PCK_SYSMAN_UTL.FC_NOMBRE_MES(SUBSTR(V.FINAL_DISFRUTE,4,2))MES_FNDIS,
SUBSTR(V.FINAL_DISFRUTE,7,4) ANO_FNDIS,
V.DIAS,
V.ACTO,
SUBSTR(V.FECHA_ACTO,1,2) DIA_ACTO,
PCK_SYSMAN_UTL.FC_NOMBRE_MES(SUBSTR(V.FECHA_ACTO,4,2))MES_ACTO,
SUBSTR(V.FECHA_ACTO,7,4) ANO_ACTO
FROM
PERSONAL P
LEFT JOIN CARGOS C
ON C.COMPANIA = P.COMPANIA
AND C.ESCALAFON = P.ESCALAFON
AND C.ID_DE_CARGO = P.ID_DE_CARGO
LEFT JOIN CATEGORIA CA
ON P.COMPANIA = CA.COMPANIA
AND P.ESCALAFON = CA.ESCALAFON
AND P.ID_DE_CATEGORIA  = CA.ID_DE_CATEGORIA
AND P.ANO = CA.ANO
LEFT JOIN VACACIONES V
ON V.COMPANIA = P.COMPANIA
AND V.ID_DE_EMPLEADO = P.ID_DE_EMPLEADO
AND V.ANO = (SELECT MAX(V.ANO) FROM VACACIONES V WHERE  V.COMPANIA = s$compania$s AND V.ID_DE_EMPLEADO = s$idEmpleado$s)
WHERE
P.COMPANIA = s$compania$s
AND P.ID_DE_EMPLEADO = s$idEmpleado$s
