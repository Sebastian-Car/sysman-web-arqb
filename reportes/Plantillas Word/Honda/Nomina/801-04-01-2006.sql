SELECT  
 s$companiaNombre$s COMPANIANOMBRE, 
 s$companiaCiudad$s COMPANIACIUDAD,
 TO_CHAR(to_date(s$fechaReporte$s,'DD/MM/YYYY'), 'DD')||' del mes de ' ||TRIM(TO_CHAR(to_date(s$fechaReporte$s,'DD/MM/YYYY'), 'Month','NLS_DATE_LANGUAGE=SPANISH')) || ' de ' || TO_CHAR(to_date(s$fechaReporte$s,'DD/MM/YYYY'), 'YYYY')FECHAREPORTE_LARGA,
  P.NOMBRECOMPLETO EMPLEADO, 
  TRIM(TO_CHAR(P.NUMERO_DCTO,'999,999,999,999')) CEDEMPLEADO,
  P.NOMBRE_EXPEDIDA_CED CEDEXPEDIDA, 
  UPPER(P.NOMBRE_DE_CARGO) NOMBRECARGO,
  C.CODIGOGRADO CODCATEGORIA,
  UPPER(P.NOMBRE_FORMANOMBRAMIENTO) FORMA_NOMBRAMIENTO,
  TO_CHAR(P.FECHA_DE_INGRESO, 'DD')||' del mes de ' || TRIM(TO_CHAR(FECHA_DE_INGRESO, 'Month','NLS_DATE_LANGUAGE=SPANISH')) || ' de ' || TO_CHAR(FECHA_DE_INGRESO, 'YYYY') FECHAINGRESO_LARGA,
  TRIM(TO_CHAR(P.SALARIO_BASE_IBC,'999,999,999.99')) SALARIOBASE,
  PCK_SYSMAN_UTL.FC_VALOR_LETRAS(P.SALARIO_BASE_IBC) SALARIOBASE_LETRAS, 
  CASE WHEN C.SUBSALIMENTACION > 0 THEN 'Percibe por prima de alimentación la suma de ' || TO_CHAR(C.SUBSALIMENTACION, '999,999,990.90') || '($ '|| PCK_SYSMAN_UTL.FC_VALOR_LETRAS(C.SUBSALIMENTACION) ||').' ELSE '' END SI_PRIMALIMENTACION,
  CASE WHEN C.SUBSTRANSPORTE > 0 THEN 'Percibe por prima de transporte la suma de ' || TO_CHAR(C.SUBSTRANSPORTE, '999,999,990.90') || '($ '|| PCK_SYSMAN_UTL.FC_VALOR_LETRAS(C.SUBSTRANSPORTE) ||').' ELSE '' END SI_PRIMATRANSPORTE,
  PCK_SYSMAN_UTL.FC_PAR(P.COMPANIA, 'NOMBRE DE QUIEN FIRMA CERTIFICADO DE SALARIOS', 6,SYSDATE) PAR_NOMBREFIRMACERT_SALARIOS, 
  PCK_SYSMAN_UTL.FC_PAR(P.COMPANIA, 'CARGO DE QUIEN FIRMA CERTIFICADO DE SALARIOS', 6,SYSDATE) PAR_CARGOFIRMACERT_SALARIOS   
FROM PERSONAL_HISTORICO P
 INNER JOIN CATEGORIA C 
   ON P.COMPANIA = C.COMPANIA
  AND P.ESCALAFON = C.ESCALAFON
  AND P.ID_DE_CATEGORIA = C.ID_DE_CATEGORIA 
  AND P.ANO = C.ANO   
 WHERE P.COMPANIA = s$compania$s
  AND P.ID_DE_PROCESO = s$idProceso$s
  AND P.ANO = s$ano$s
  AND P.MES = s$mes$s
  AND P.PERIODO = s$periodo$s
  AND P.ID_DE_EMPLEADO  =
    CASE s$idEmpleado$s
      WHEN 0
      THEN P.ID_DE_EMPLEADO
      ELSE s$idEmpleado$s
    END