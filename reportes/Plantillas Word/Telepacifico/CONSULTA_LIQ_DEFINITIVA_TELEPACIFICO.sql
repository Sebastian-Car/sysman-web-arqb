SELECT ANO,
(SELECT NVL(LISTAGG( DIAS || ' días de licencia no remunerada' || ' del '|| TRIM(TO_CHAR(FECHA_INICIO,'DD" de" month','NLS_DATE_LANGUAGE=SPANISH')) || ' ' || TRIM(TO_CHAR(FECHA_INICIO,'"de "YYYY','NLS_DATE_LANGUAGE=SPANISH')) || 
' al '|| TRIM(TO_CHAR(FECHA_FINAL,'DD" de" month','NLS_DATE_LANGUAGE=SPANISH')) || ' ' || TRIM(TO_CHAR(FECHA_FINAL,'"de "YYYY','NLS_DATE_LANGUAGE=SPANISH')) || '. ' on overflow truncate) WITHIN GROUP (ORDER BY FECHA_INICIO, FECHA_FINAL), ' ')
FROM (
SELECT TO_NUMBER((TRUNC(LICENCIAS.FECHA_FINAL) - TRUNC(LICENCIAS.FECHA_INICIO))) DIAS, LICENCIAS.FECHA_INICIO, LICENCIAS.FECHA_FINAL 
FROM LICENCIAS
INNER JOIN TIPOS_LICENCIA ON 
LICENCIAS.LICENCIA = TIPOS_LICENCIA.LICENCIA
AND LICENCIAS.COMPANIA = TIPOS_LICENCIA.COMPANIA
WHERE LICENCIAS.COMPANIA     = s$compania$s
AND LICENCIAS.ID_DE_EMPLEADO = s$idEmpleado$s
AND TIPOS_LICENCIA.REMUNERACION IN (0))) LICENCIAS,
ID_DE_EMPLEADO,
UPPER(NOMBRECOMPLETO) NOMBRECOMPLETO,
TRIM(TO_CHAR(NUMERO_DCTO,'999,999,999,999,999,999.99')) NUMERO_DCTO,
TRIM(TO_CHAR(FECHA_INGRESO,'DD" de" month','NLS_DATE_LANGUAGE=SPANISH')) || ' ' || TRIM(TO_CHAR(FECHA_INGRESO,'"de "YYYY','NLS_DATE_LANGUAGE=SPANISH')) FECHA_INGRESO,
NOMBRE_CARGO,
PCK_SYSMAN_UTL.FC_VALOR_LETRAS(SALARIO) SALARIO_BASE_LETRAS,
TRIM(TO_CHAR(SALARIO,'999,999,999,999,999,999.99'))FACTOR10, 
PCK_SYSMAN_UTL.FC_VALOR_LETRAS(C901) GASTOSREP_LETRAS,
TRIM(TO_CHAR(C901,'999,999,999,999,999,999.99')) GASTOSREP,
PCK_SYSMAN_UTL.FC_VALOR_LETRAS(SALARIO + C901)TOTALSALGREP_LETRAS,
TRIM(TO_CHAR(SALARIO + C901,'999,999,999,999,999,999.99')) TOTALPAGOSALGREP,
TRIM(TO_CHAR(FECHA_RETIRO,'DD" de" month','NLS_DATE_LANGUAGE=SPANISH')) || ' ' || TRIM(TO_CHAR(FECHA_RETIRO,'"de "YYYY','NLS_DATE_LANGUAGE=SPANISH')) FECHAFIN,
TRIM(TO_CHAR(FECHAPNAVIDAD,'DD" de" month','NLS_DATE_LANGUAGE=SPANISH')) || ' ' || TRIM(TO_CHAR(FECHAPNAVIDAD,'"de "YYYY','NLS_DATE_LANGUAGE=SPANISH')) FECHAINIC,
TRIM(TO_CHAR(FECHA_RETIRO,'month','NLS_DATE_LANGUAGE=SPANISH'))||' ' || TRIM(TO_CHAR(FECHA_RETIRO,'"de "YYYY','NLS_DATE_LANGUAGE=SPANISH')) AS MESANOPAGO,
TRIM(TO_CHAR(FECHA_RETIRO,'"01 de" month','NLS_DATE_LANGUAGE=SPANISH'))||' ' || TRIM(TO_CHAR(FECHA_RETIRO,'"de "YYYY','NLS_DATE_LANGUAGE=SPANISH')) AS FECHAINIP,
C009 DIASTRABAJADOS,
TRIM(TO_CHAR(C908,'999,999,999,999,999,999.99')) FACTORBASP,
TRIM(TO_CHAR(C906,'999,999,999,999,999,999.99')) FACTORPSERV,
'(del '|| TRIM(TO_CHAR(FECHA_CUMPLIMIENTO_BONIFICACIO,'DD" de" month','NLS_DATE_LANGUAGE=SPANISH')) || ' ' || TRIM(TO_CHAR(FECHA_CUMPLIMIENTO_BONIFICACIO,'"de "YYYY','NLS_DATE_LANGUAGE=SPANISH')) || 
' al '|| TRIM(TO_CHAR(FECHA_RETIRO,'DD" de" month','NLS_DATE_LANGUAGE=SPANISH')) || ' ' || TRIM(TO_CHAR(FECHA_RETIRO,'"de "YYYY','NLS_DATE_LANGUAGE=SPANISH')) || ')' FECHABASP,
C161 PORCENTAJEBASP,
((TRUNC(FECHA_RETIRO) - TRUNC(FECHA_CUMPLIMIENTO_BONIFICACIO))-1) DIASFECHABASP, 
TRIM(TO_CHAR(C150,'999,999,999,999,999,999.99')) CBASP,
'(del '|| TRIM(TO_CHAR(FECHAPSERVICIOS,'DD" de" month','NLS_DATE_LANGUAGE=SPANISH')) || ' ' || TRIM(TO_CHAR(FECHAPSERVICIOS,'"de "YYYY','NLS_DATE_LANGUAGE=SPANISH')) || 
' al '|| TRIM(TO_CHAR(FECHA_RETIRO,'DD" de" month','NLS_DATE_LANGUAGE=SPANISH')) || ' ' || TRIM(TO_CHAR(FECHA_RETIRO,'"de "YYYY','NLS_DATE_LANGUAGE=SPANISH')) || ')' FECHAPSERVICIOS,
TRIM(TO_CHAR(SALARIO + C901 + C908,'999,999,999,999,999,999.99')) TOTALFACTORPSERV,
((TRUNC(FECHA_RETIRO) - TRUNC(FECHAPSERVICIOS))) DIASPRIJUN,
TRIM(TO_CHAR(C160,'999,999,999,999,999,999.99')) TOTALPAGOPSERV,
'(del '|| TRIM(TO_CHAR(FECHAULTIMASVAC,'DD" de" month','NLS_DATE_LANGUAGE=SPANISH')) || ' ' || TRIM(TO_CHAR(FECHAULTIMASVAC,'"de "YYYY','NLS_DATE_LANGUAGE=SPANISH')) || 
' al '|| TRIM(TO_CHAR(FECHA_RETIRO,'DD" de" month','NLS_DATE_LANGUAGE=SPANISH')) || ' ' || TRIM(TO_CHAR(FECHA_RETIRO,'"de "YYYY','NLS_DATE_LANGUAGE=SPANISH')) || ')' FECHAVACACIONES,
TRIM(TO_CHAR(SALARIO + C901 + C908 + C906,'999,999,999,999,999,999.99')) TOTALFACTORESVAC,
C96 DIASVAC,
TRIM(TO_CHAR(C175,'999,999,999,999,999,999.99')) TOTALPAGOVAC,
22 DIASPRIVAC,
TRIM(TO_CHAR(C155,'999,999,999,999,999,999.99'))PRIMVAC,
C998 DIASREC,
TRIM(TO_CHAR(C151,'999,999,999,999,999,999.99')) BONRECRE, 
((TRUNC(FECHA_RETIRO) - TRUNC(FECHAPNAVIDAD))-1) DIASCESANTIAS,
TRIM(TO_CHAR(C907,'999,999,999,999,999,999.99')) DOCPRIVAC,
TRIM(TO_CHAR(SALARIO + C901 + C908 + C906 + C907,'999,999,999,999,999,999.99')) TOTALFACTORESNAV,
TRIM(TO_CHAR(C158,'999,999,999,999,999,999.99')) TOTALPAGONAV, 
0 DOCEXTRAS,
TRIM(TO_CHAR(C905,'999,999,999,999,999,999.99')) DOCPRINAV,
TRIM(TO_CHAR(SALARIO + C901 + C908 + C906 +C907+C905,'999,999,999,999,999,999.99')) TOTALFACTORESCES,
TRIM(TO_CHAR(C177,'999,999,999,999,999,999.99')) TOTALPAGOCES,
TRIM(TO_CHAR(C169,'999,999,999,999,999,999.99')) TOTALINTERESCES,
TRIM(TO_CHAR(C150 + C151 + C155 + C158 + C160 + C169 + C175,'999,999,999,999,999,999.99'))SUBTOTALCES,
TRIM(TO_CHAR(C125,'999,999,999,999,999,999.99'))DESRETEFTE,
TRIM(TO_CHAR(C600,'999,999,999,999,999,999.99'))LIBRANZA,
TRIM(TO_CHAR(C97,'999,999,999,999,999,999.99'))SUBTOTAL,
TRIM(TO_CHAR(C144,'999,999,999,999,999,999.99')) TOTALPAG,
PCK_SYSMAN_UTL.FC_VALOR_LETRAS(C144) TOTALPAGLETRAS,
TRIM(TO_CHAR(C140,'999,999,999,999,999,999.99')) TOTALDESC,
TRIM(TO_CHAR(FECHA_RETIRO,'YYYY','NLS_DATE_LANGUAGE=SPANISH')) AS ANOCES,
LOWER(TRIM(PCK_SYSMAN_UTL.FC_VALOR_LETRAS(TO_NUMBER(TO_CHAR(SYSDATE,'DD')),-1))) || ' (' || TRIM(TO_CHAR(SYSDATE,'DD") dias del mes de" month','NLS_DATE_LANGUAGE=SPANISH')) || ' ' || TRIM(TO_CHAR(SYSDATE,'"de "YYYY','NLS_DATE_LANGUAGE=SPANISH')) FECHA_ACTUAL,
PAR_NOMBREGERENTE,
PAR_CARGOGERENTE
FROM (
SELECT 
    PERSONAL_HISTORICO.ANO,
    PERSONAL_HISTORICO.COMPANIA,
    PERSONAL_HISTORICO.ID_DE_EMPLEADO,
    PERSONAL_HISTORICO.NOMBRECOMPLETO NOMBRECOMPLETO,    
    PERSONAL_HISTORICO.NUMERO_DCTO NUMERO_DCTO,
    PERSONAL_HISTORICO.FECHA_DE_INGRESO FECHA_INGRESO,
    PERSONAL_HISTORICO.FECHA_DE_RETIRO FECHA_RETIRO,
    PERSONAL_HISTORICO.NOMBRE_DE_CARGO AS NOMBRE_CARGO,
    PERSONAL_HISTORICO.SALARIO_BASE_IBC SALARIO,
    PERSONAL_HISTORICO.FECHA_CUMPLIMIENTO_BONIFICACIO,    
    (CASE WHEN HISTORICOS.MES > 6 THEN TO_DATE('01/07/'||PERSONAL_HISTORICO.ANO,'DD/MM/YYYY') ELSE TO_DATE('01/07/'||PERSONAL_HISTORICO.ANO-1,'DD/MM/YYYY') END) FECHAPSERVICIOS,
    PERSONAL_HISTORICO.FECHA_FINAL FECHAULTIMASVAC,
    TO_DATE('01/01/'||PERSONAL_HISTORICO.ANO,'DD/MM/YYYY') FECHAPNAVIDAD,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('96') THEN HISTORICOS.VALOR ELSE 0 END) AS C96, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('97') THEN HISTORICOS.VALOR ELSE 0 END) AS C97,
    TRIM(TO_CHAR(SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('009') THEN HISTORICOS.VALOR ELSE 0 END))) C009,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('125') THEN HISTORICOS.VALOR ELSE 0 END) AS C125,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('140') THEN HISTORICOS.VALOR ELSE 0 END) AS C140,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('144') THEN HISTORICOS.VALOR ELSE 0 END) AS C144,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('150') THEN HISTORICOS.VALOR ELSE 0 END) AS C150, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('151') THEN HISTORICOS.VALOR ELSE 0 END) AS C151, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('155') THEN HISTORICOS.VALOR ELSE 0 END) AS C155, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('158') THEN HISTORICOS.VALOR ELSE 0 END) AS C158,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('160') THEN HISTORICOS.VALOR ELSE 0 END) AS C160,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('161') THEN HISTORICOS.VALOR ELSE 0 END) AS C161, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('169') THEN HISTORICOS.VALOR ELSE 0 END) AS C169,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('175') THEN HISTORICOS.VALOR ELSE 0 END) AS C175,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('177') THEN HISTORICOS.VALOR ELSE 0 END) AS C177, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('600') THEN HISTORICOS.VALOR ELSE 0 END) AS C600,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('901') THEN HISTORICOS.VALOR ELSE 0 END) AS C901,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('902') THEN HISTORICOS.VALOR ELSE 0 END) AS C902,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('905') THEN HISTORICOS.VALOR ELSE 0 END) AS C905,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('906') THEN HISTORICOS.VALOR ELSE 0 END) AS C906,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('907') THEN HISTORICOS.VALOR ELSE 0 END) AS C907,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('908') THEN HISTORICOS.VALOR ELSE 0 END) AS C908,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('998') THEN HISTORICOS.VALOR ELSE 0 END) AS C998,    
    LOWER(TRIM(PCK_SYSMAN_UTL.FC_VALOR_LETRAS(TO_NUMBER(TO_CHAR(SYSDATE,'DD')),-1))) || ' (' || TRIM(TO_CHAR(SYSDATE,'DD") dias del mes de" month','NLS_DATE_LANGUAGE=SPANISH')) || ' ' || TRIM(TO_CHAR(SYSDATE,'"de "YYYY','NLS_DATE_LANGUAGE=SPANISH')) FECHAACTLETRAS,
    '01/01/' || PERSONAL_HISTORICO.ANO || ' al ' || '31/12/' || PERSONAL_HISTORICO.ANO FECHASLIQ,
    PCK_SYSMAN_UTL.FC_PAR(HISTORICOS.COMPANIA,'NOMBRE DEL GERENTE', 6,SYSDATE) PAR_NOMBREGERENTE,
    PCK_SYSMAN_UTL.FC_PAR(HISTORICOS.COMPANIA,'CARGO DEL GERENTE', 6,SYSDATE) PAR_CARGOGERENTE
FROM HISTORICOS
        INNER JOIN PERSONAL_HISTORICO   
            ON  (PERSONAL_HISTORICO.COMPANIA = HISTORICOS.COMPANIA)   
            AND (PERSONAL_HISTORICO.ID_DE_PROCESO = HISTORICOS.ID_DE_PROCESO)   
            AND (PERSONAL_HISTORICO.ANO = HISTORICOS.ANO)       
            AND (PERSONAL_HISTORICO.MES = HISTORICOS.MES)   
            AND (PERSONAL_HISTORICO.PERIODO = HISTORICOS.PERIODO)           
            AND (PERSONAL_HISTORICO.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO)
WHERE 
        HISTORICOS.COMPANIA     = s$compania$s
    AND HISTORICOS.ANO          = s$ano$s
    AND HISTORICOS.MES          = s$mes$s
    AND HISTORICOS.PERIODO      = s$periodo$s
    AND HISTORICOS.ID_DE_EMPLEADO = s$idEmpleado$s
    AND HISTORICOS.ID_DE_CONCEPTO IN (70,96,97, 125,140,144,150,151,155,158,160,161,169,175,177,600,901,902,905,906,907,908,946,950,998)
    AND EXISTS (
                SELECT  NULL
                FROM    HISTORICOS HISTORICOS2
                WHERE   HISTORICOS.COMPANIA = HISTORICOS2.COMPANIA
                        AND HISTORICOS.ID_DE_PROCESO = HISTORICOS2.ID_DE_PROCESO
                        AND HISTORICOS.ANO = HISTORICOS2.ANO
                        AND HISTORICOS.MES = HISTORICOS2.MES
                        AND HISTORICOS.PERIODO = HISTORICOS2.PERIODO
                        AND HISTORICOS.ID_DE_EMPLEADO = HISTORICOS2.ID_DE_EMPLEADO
                        AND HISTORICOS2.ID_DE_CONCEPTO = 404
                )
GROUP BY 
    PERSONAL_HISTORICO.ANO,
    PERSONAL_HISTORICO.COMPANIA,
    PERSONAL_HISTORICO.ID_DE_EMPLEADO,
    PERSONAL_HISTORICO.NOMBRECOMPLETO,    
    PERSONAL_HISTORICO.NUMERO_DCTO,
    PERSONAL_HISTORICO.FECHA_DE_INGRESO,
    PERSONAL_HISTORICO.FECHA_DE_RETIRO,
    PERSONAL_HISTORICO.NOMBRE_DE_CARGO,
    PERSONAL_HISTORICO.SALARIO_BASE_IBC,
    PERSONAL_HISTORICO.FECHA_CUMPLIMIENTO_BONIFICACIO,
    (CASE WHEN HISTORICOS.MES > 6 THEN TO_DATE('01/07/'||PERSONAL_HISTORICO.ANO,'DD/MM/YYYY') ELSE TO_DATE('01/07/'||PERSONAL_HISTORICO.ANO-1,'DD/MM/YYYY') END),
    PERSONAL_HISTORICO.FECHA_FINAL,
    PCK_SYSMAN_UTL.FC_PAR(HISTORICOS.COMPANIA,'NOMBRE DEL GERENTE', 6,SYSDATE),
    PCK_SYSMAN_UTL.FC_PAR(HISTORICOS.COMPANIA,'CARGO DEL GERENTE', 6,SYSDATE)
 )