SELECT 
COMPANIA,
ANO,
TERCERO,
NOMBRE_TERCERO,
TASAINTERES,
TO_CHAR(CAPITAL,'9G999G999') CAPITAL,
NVL(INTERES,0) INTERES,
DIRECCION,
TO_CHAR((CAPITAL + NVL(INTERES,0)),'9G999G999') TOTAL,
NUMERO_RESOLUCION NO_RESOLUCION,
NUMERO_EXPEDIENTE NO_EXPEDIENTE,
PCK_SYSMAN_UTL.FC_PAR(COMPANIA,'NOMBRE SUBDIRECTOR ADMINISTRATIVO Y FINANCIERO',69,SYSDATE,0) PARAM_NOMBRE_DIRECTOR,
PCK_SYSMAN_UTL.FC_PAR(COMPANIA,'CARGO SUBDIRECTOR ADMINISTRATIVO Y FINANCIERO',69,SYSDATE,0) PARAM_CARGO_DIRECTOR,
PCK_SYSMAN_UTL.FC_PAR(COMPANIA,'ANEXOS PLANTILLA PERSUASIVO',69,SYSDATE,0) PARAM_ANEXOS,
PCK_SYSMAN_UTL.FC_PAR(COMPANIA,'ELABORO PERSUASIVO',69,SYSDATE,0) PARAM_ELABORO,
PCK_SYSMAN_UTL.FC_PAR(COMPANIA,'REVISO PERSUASIVO',69,SYSDATE,0) PARAM_REVISO,
PCK_SYSMAN_UTL.FC_VALOR_LETRAS(CAPITAL) VALOR_LETRAS_CAPITAL,
(CASE WHEN INTERES > 0 THEN PCK_SYSMAN_UTL.FC_VALOR_LETRAS(INTERES)ELSE 'CERO PESOS MC' END) VALOR_LETRAS_INTERES,
TO_CHAR(TO_DATE('s$fechaVencimiento$s', 'DD/MM/YYYY'), 'DD-Mon-YYYY') FECHA_VENC,
TO_CHAR(TO_DATE('s$fechaCorte$s', 'DD/MM/YYYY'), 'DD-Mon-YYYY') FECHA_CORTE,
PCK_SYSMAN_UTL.FC_DIASMESCOMERCIAL('s$fechaVencimiento$s','s$fechaCorte$s') DIAS,
TO_CHAR(TO_DATE('s$fechaVencimiento$s', 'DD/MM/YYYY'),'DD')
||' de '|| TRIM(TO_CHAR(TO_DATE('s$fechaVencimiento$s', 'DD/MM/YYYY'), 'Month'))||' de '||TO_CHAR(TO_DATE('s$fechaVencimiento$s', 'DD/MM/YYYY'), 'YYYY') FECHA_LETRA_VEC,
TO_CHAR(TO_DATE('s$fechaVencimiento$s', 'DD/MM/YYYY'),'DD')
||' de '|| TRIM(TO_CHAR(TO_DATE('s$fechaVencimiento$s', 'DD/MM/YYYY'), 'Month'))||' de '||TO_CHAR(TO_DATE('s$fechaVencimiento$s', 'DD/MM/YYYY'), 'YYYY') FECHA_LETRA_CORTE
FROM (
SELECT FRA.COMPANIA, FRA.ANO, FRA.TIPO_FACTURA,  FRA.TERCERO, FRA.SUCURSAL,TER.NOMBRE NOMBRE_TERCERO,
TER.DIRECCION,
            FRA.CONCEPTO,
            SFTI.TASAINTERES,
            FRA.VALOR_BASE DEUDA,
            HFRA.NUMERO_RESOLUCION, 
            HFRA.NUMERO_EXPEDIENTE
     FROM   SF_DETALLE_FACTURA  FRA 
            INNER JOIN SF_FACTURA HFRA                                 
                ON FRA.COMPANIA = HFRA.COMPANIA                                 
                AND FRA.ANO  = HFRA.ANO                                 
                AND FRA.TIPO_FACTURA = HFRA.TIPO_FACTURA                                
                AND FRA.FACTURA = HFRA.NUMERO_FACTURA    
            INNER JOIN TERCERO TER
             ON TER.COMPANIA = HFRA.COMPANIA
            AND TER.NIT  = HFRA.TERCERO
            AND TER.SUCURSAL = HFRA.SUCURSAL
            INNER JOIN SF_TASAS_INTERES SFTI
             ON SFTI.COMPANIA = HFRA.COMPANIA
            AND SFTI.ANO = HFRA.ANO
            AND SFTI.TIPOCOBRO = HFRA.TIPO_FACTURA
            INNER JOIN (
    SELECT AB.COMPANIA, AB.TIPO_FACT, AB.NRO_FACT, 
         SUM(CASE WHEN AB.ESTADO = 'P' THEN DC.VALOR_BASE ELSE 0 END) VLRBASE, 
         SUM(CASE WHEN AB.ESTADO = 'P' THEN DC.VALOR_IVA  ELSE 0 END) VLRIVA, 
         SUM(CASE WHEN AB.ESTADO = 'P' THEN DC.VALOR_RETE  ELSE 0 END) VLRRETE, 
         SUM(CASE WHEN AB.ESTADO = 'P' THEN DC.VALOR_DESCUENTO  ELSE 0 END) VLRDESC, 
         SUM(CASE WHEN AB.ESTADO = 'P' THEN DC.VALOR_ICA  ELSE 0 END) VLRICA, 
         SUM(CASE WHEN AB.ESTADO = 'P' THEN DC.TOTAL_CUOTA  ELSE 0 END) VLRTOTAL
     FROM   SF_ABONOS AB LEFT JOIN SF_DETALLE_CUOTAABONO DC 
        ON AB.COMPANIA = DC.COMPANIA
        AND AB.TIPO = DC.TIPO_ABONO
        AND AB.CODIGO = DC.CODIGO_ABONO
     WHERE AB.COMPANIA      = s$compania$s
     AND AB.TIPO = 's$tipoAbono$s'
     AND AB.CODIGO     = 's$codigo$s'
     GROUP BY AB.COMPANIA, AB.TIPO_FACT, AB.NRO_FACT
     )
                 PAGOS
                ON FRA.COMPANIA =PAGOS.COMPANIA
                AND FRA.TIPO_FACTURA = PAGOS.TIPO_FACT
                AND FRA.FACTURA = PAGOS.NRO_FACT
            INNER JOIN SF_CONCEPTOS C
                ON  FRA.COMPANIA = C.COMPANIA
                AND FRA.ANO = C.ANO
                AND FRA.TIPO_FACTURA = C.TIPOCOBRO
                AND FRA.CONCEPTO = C.CODIGO
    WHERE FRA.COMPANIA = s$compania$s
    AND   FRA.ANO = s$anio$s 
    AND   HFRA.ANULADA IN (0) ORDER BY CONCEPTO
    )
  PIVOT (
MAX(DEUDA)
FOR 
CONCEPTO
IN ( 
'101' AS "CAPITAL",
'111' AS "INTERES"))