SELECT DISTINCT
P.NOMBRES || ' ' ||P.APELLIDO1 || ' '||  P.APELLIDO2 NOM_EMPLEADO,
P.NUMERO_DCTO NUM_DOC,
CASE WHEN EX.NOMBRE IS NOT NULL THEN INITCAP(EX.NOMBRE || ', ' ||DPTO.NOMBRE) ELSE '' END EXPEDIDA,
P.ID_DE_CARGO CODIGOCARGO,
REPLACE(C.NOMBRE_DEL_CARGO,TRIM(REGEXP_REPLACE(C.NOMBRE_DEL_CARGO,'[A-Za-z]')),'') NOM_CARGO,
CA.GRADO GRADO,
SUBSTR(V.FECHA_INICIO,1,2) ||' de '|| 
PCK_SYSMAN_UTL.FC_NOMBRE_MES(PCK_SYSMAN_UTL.FC_MES(V.FECHA_INICIO)) ||' de '||
PCK_SYSMAN_UTL.FC_ANIO(V.FECHA_INICIO) FECHA_INICIO,
SUBSTR(V.FECHA_FINAL,1,2) ||' de '|| 
PCK_SYSMAN_UTL.FC_NOMBRE_MES(PCK_SYSMAN_UTL.FC_MES(V.FECHA_FINAL)) ||' de '||
PCK_SYSMAN_UTL.FC_ANIO(V.FECHA_FINAL) FECHA_FINAL,
SUBSTR(V.INICIO_DISFRUTE,1,2) ||' de '|| 
PCK_SYSMAN_UTL.FC_NOMBRE_MES(PCK_SYSMAN_UTL.FC_MES(V.INICIO_DISFRUTE)) ||' de '||
PCK_SYSMAN_UTL.FC_ANIO(V.INICIO_DISFRUTE) INICIO_DISFRUTE,
SUBSTR(V.FINAL_DISFRUTE,1,2) ||' de '|| 
PCK_SYSMAN_UTL.FC_NOMBRE_MES(PCK_SYSMAN_UTL.FC_MES(V.FINAL_DISFRUTE)) ||' de '||
PCK_SYSMAN_UTL.FC_ANIO(V.FINAL_DISFRUTE) FINAL_DISFRUTE,
CASE WHEN V.DIAS = 0 THEN V.DIASDINERO ELSE V.DIAS END DIAS,
V.DIASHABILES,
V.ACTO,
SUBSTR(V.FECHA_ACTO,1,2) ||' de '|| 
PCK_SYSMAN_UTL.FC_NOMBRE_MES(PCK_SYSMAN_UTL.FC_MES(V.FECHA_ACTO)) ||' de '||
PCK_SYSMAN_UTL.FC_ANIO(V.FECHA_ACTO) FECHA_ACTO,
SUM(CASE WHEN H.ID_DE_CONCEPTO IN ('175','174') THEN  H.VALOR ELSE 0 END) IND_VAC,
SUM(CASE WHEN H.ID_DE_CONCEPTO = ('155') THEN H.VALOR ELSE 0 END) PRIM_VAC,
SUM(CASE WHEN H.ID_DE_CONCEPTO = ('151') THEN H.VALOR ELSE 0 END) BON_REC,
SUM(CASE WHEN H.ID_DE_CONCEPTO IN ('144') THEN H.VALOR ELSE 0 END) AS TOTAL,
PCK_SYSMAN_UTL.FC_VALOR_LETRAS(SUM(CASE WHEN H.ID_DE_CONCEPTO IN ('144') THEN H.VALOR ELSE 0 END)) AS VTOTALLETRAS
FROM HISTORICOS H
LEFT JOIN PERSONAL P
ON P.COMPANIA = H.COMPANIA
AND P.ID_DE_EMPLEADO = H.ID_DE_EMPLEADO
LEFT JOIN CIUDAD EX
ON EX.PAIS = P.PAIS_CED
AND EX.DEPARTAMENTO = P.DEPARTAMENTO_CED
AND EX.CODIGO = P.EXPEDIDA
LEFT JOIN DEPARTAMENTO DPTO
ON EX.PAIS = DPTO.PAIS
AND EX.DEPARTAMENTO = DPTO.CODIGO
LEFT JOIN CARGOS C
ON C.COMPANIA = P.COMPANIA
AND C.ESCALAFON = P.ESCALAFON
AND C.ID_DE_CARGO = P.ID_DE_CARGO
LEFT JOIN CATEGORIA CA
ON P.COMPANIA = CA.COMPANIA
AND P.ESCALAFON = CA.ESCALAFON
AND P.ID_DE_CATEGORIA  = CA.ID_DE_CATEGORIA
AND P.ANO = CA.ANO
LEFT JOIN VACACIONES V
ON V.COMPANIA = P.COMPANIA
AND V.ID_DE_EMPLEADO = P.ID_DE_EMPLEADO
AND V.ANO = (SELECT MAX(V.ANO) FROM VACACIONES V WHERE  V.COMPANIA = s$compania$s AND V.ID_DE_EMPLEADO = s$idEmpleado$s)
WHERE
H.COMPANIA = s$compania$s
AND H.ID_DE_EMPLEADO = s$idEmpleado$s
AND H.MES = s$mes$s
AND H.ANO = s$ano$s
AND H.PERIODO = s$periodo$s
AND H.ID_DE_PROCESO = s$idProceso$s
GROUP BY 
P.NOMBRES || ' ' ||P.APELLIDO1 || ' '|| P.APELLIDO2, 
P.NUMERO_DCTO,
CASE WHEN EX.NOMBRE IS NOT NULL THEN INITCAP(EX.NOMBRE || ', ' ||DPTO.NOMBRE) ELSE '' END,
P.ID_DE_CARGO,
REPLACE(C.NOMBRE_DEL_CARGO,
TRIM(REGEXP_REPLACE(C.NOMBRE_DEL_CARGO,'[A-Za-z]')),''),
CA.GRADO,
SUBSTR(V.FECHA_INICIO,1,2) ||' de '|| 
PCK_SYSMAN_UTL.FC_NOMBRE_MES(PCK_SYSMAN_UTL.FC_MES(V.FECHA_INICIO)) ||' de '||
PCK_SYSMAN_UTL.FC_ANIO(V.FECHA_INICIO),
SUBSTR(V.FECHA_FINAL,1,2) ||' de '|| 
PCK_SYSMAN_UTL.FC_NOMBRE_MES(PCK_SYSMAN_UTL.FC_MES(V.FECHA_FINAL)) ||' de '||
PCK_SYSMAN_UTL.FC_ANIO(V.FECHA_FINAL),
SUBSTR(V.INICIO_DISFRUTE,1,2) ||' de '|| 
PCK_SYSMAN_UTL.FC_NOMBRE_MES(PCK_SYSMAN_UTL.FC_MES(V.INICIO_DISFRUTE)) ||' de '||
PCK_SYSMAN_UTL.FC_ANIO(V.INICIO_DISFRUTE),
SUBSTR(V.FINAL_DISFRUTE,1,2) ||' de '|| 
PCK_SYSMAN_UTL.FC_NOMBRE_MES(PCK_SYSMAN_UTL.FC_MES(V.FINAL_DISFRUTE)) ||' de '||
PCK_SYSMAN_UTL.FC_ANIO(V.FINAL_DISFRUTE),
CASE WHEN V.DIAS = 0 THEN V.DIASDINERO ELSE V.DIAS END,
V.ACTO,
SUBSTR(V.FECHA_ACTO,1,2) ||' de '|| 
PCK_SYSMAN_UTL.FC_NOMBRE_MES(PCK_SYSMAN_UTL.FC_MES(V.FECHA_ACTO)) ||' de '||
PCK_SYSMAN_UTL.FC_ANIO(V.FECHA_ACTO),
V.DIASHABILES