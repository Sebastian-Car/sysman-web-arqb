CREATE OR REPLACE VIEW V_FACTURAPREDIAL AS 
(SELECT T.TRPRAN,
  T.TRPRAN1,
  T.TRPRAN2,
  UP.CODIGO,
  UP.NUMERO_ORDEN,
  UP.NIT,
  UP.DIRECCION,
  UP.AREA_HA,
  UP.AREA_M2,
  UP.AREA_CONSTRUIDA,
  UP.PAGO_ANO,
  UP.PAG_VAL,
  UP.PAG_FEC,
  UP.NUM_COM,
  UP.DIRECCION_CORRESPONDENCIA,
  UP.COD_POSTAL,
  UP.RECIBO_ACTUAL,
  UP.NOMBRE,
  RP.PREANO,
  RP.PREFECPAG,
  RP.PREFECLIM,
  F.C1,
  F.C2,
  F.C3,
  F.C4,
  F.C5,
  F.C6,
  F.C7,
  F.C8,
  F.C9,
  F.C10,
  F.C11,
  F.C12,
  F.C13,
  F.C14,
  F.C15,
  F.C16,
  F.C17,
  F.C18,
  F.C19,
  F.C20,
  F.TOTAL
FROM IP_USUARIOS_PREDIAL UP
LEFT JOIN IP_TARIFAS T
ON UP.COMPANIA = T.COMPANIA
AND UP.TRPCOD  = T.TRPCOD 
AND UP.TRPRAN  = T.TRPRAN
AND UP.TRPANO  = T.TRPANO
INNER JOIN IP_RECIBOS_DE_PAGO RP ON
  UP.COMPANIA         = RP.COMPANIA
  AND UP.NUMERO_ORDEN = RP.NUMERO_ORDEN
  AND UP.CODIGO       = RP.PRECOD
INNER JOIN IP_FACTURADOS F ON 
UP.COMPANIA = F.COMPANIA
AND UP.NUMERO_ORDEN = F.NUMERO_ORDEN
AND UP.CODIGO = F.CODIGO);