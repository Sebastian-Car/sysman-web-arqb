LEFT JOIN POLIZAS
 ON ORDENDECOMPRA.COMPANIA = POLIZAS.COMPANIA
AND ORDENDECOMPRA.NUMERO = POLIZAS.ORDENDECOMPRA
AND ORDENDECOMPRA.CLASEORDEN = POLIZAS.CLASEORDEN
LEFT JOIN  (
     SELECT s.COMPANIA,s.CLASEORDEN,s.NUMEROCONTRATO,s.CEDULA,s.DATE_CREATED,S.SUCURSAL
    FROM SUPERVISORES s
    WHERE s.DATE_CREATED = (
        SELECT MIN(s2.DATE_CREATED)
        FROM SUPERVISORES s2
        WHERE s2.NUMEROCONTRATO = s.NUMEROCONTRATO
          AND s2.CLASEORDEN = s.CLASEORDEN
          AND s2.COMPANIA = s.COMPANIA
    )
) SUPERVISORES
ON ORDENDECOMPRA.NUMERO = SUPERVISORES.NUMEROCONTRATO
AND ORDENDECOMPRA.CLASEORDEN = SUPERVISORES.CLASEORDEN
AND ORDENDECOMPRA.COMPANIA = SUPERVISORES.COMPANIA
LEFT JOIN TERCERO SUPERVISOR
ON SUPERVISORES.COMPANIA = SUPERVISOR.COMPANIA
AND SUPERVISORES.CEDULA = SUPERVISOR.NIT
AND SUPERVISORES.SUCURSAL = SUPERVISOR.SUCURSAL
LEFT JOIN TIPOS_DOCUMENTOS TIPO_DCTO_SUPERVISOR
 ON SUPERVISOR.COMPANIA = TIPO_DCTO_SUPERVISOR.COMPANIA
AND SUPERVISOR.TIPOID = TIPO_DCTO_SUPERVISOR.DCTO_IDENTIDAD
LEFT JOIN CLASIFICACION_PROPONENTES
ON ORDENDECOMPRA.NUMERO = CLASIFICACION_PROPONENTES.NUMERO
AND ORDENDECOMPRA.CLASEORDEN = CLASIFICACION_PROPONENTES.CLASEORDEN
AND ORDENDECOMPRA.COMPANIA = CLASIFICACION_PROPONENTES.COMPANIA
LEFT JOIN CODIGO_RUP
ON CLASIFICACION_PROPONENTES.GRUPO = CODIGO_RUP.CODIGO
/*INNER JOIN TIPOSIRECI
ON TIPOORDENDECOMPRA.COMPANIA = TIPOSIRECI.COMPANIA
AND TIPOORDENDECOMPRA.TIPOSIRECI = TIPOSIRECI.CODIGO */
INNER JOIN TERCERO
ON ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA
AND ORDENDECOMPRA.TERCERO = TERCERO.NIT
AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL
LEFT JOIN CONSORCIADOS
ON TERCERO.COMPANIA = CONSORCIADOS.COMPANIA
AND TERCERO.NIT = CONSORCIADOS.NIT
AND TERCERO.SUCURSAL = CONSORCIADOS.SUCURSAL
LEFT JOIN TERCERO CONSORCIADO
ON CONSORCIADOS.COMPANIA = CONSORCIADO.COMPANIA
AND CONSORCIADOS.NITCONSORCIO = CONSORCIADO.NIT
AND CONSORCIADOS.SUCURSALCONSORCIO = CONSORCIADO.SUCURSAL
LEFT JOIN TIPOS_DOCUMENTOS TIPO_DCTO_CONSORCIADO
 ON CONSORCIADO.COMPANIA = TIPO_DCTO_CONSORCIADO.COMPANIA
AND CONSORCIADO.TIPOID = TIPO_DCTO_CONSORCIADO.DCTO_IDENTIDAD
LEFT JOIN ORDENADOR
ON ORDENDECOMPRA.COMPANIA = ORDENADOR.COMPANIA
AND ORDENDECOMPRA.ORDENADOR = ORDENADOR.CEDULA
AND ORDENDECOMPRA.SUCURSAL_ORDENADOR = ORDENADOR.SUCURSAL
INNER JOIN TERCERO CONTRATISTA
 ON ORDENDECOMPRA.COMPANIA = CONTRATISTA.COMPANIA
AND ORDENDECOMPRA.CEDULACONTRATISTA = CONTRATISTA.NIT
AND ORDENDECOMPRA.SUCURSALCONTRATISTA = CONTRATISTA.SUCURSAL
LEFT JOIN TIPOS_DOCUMENTOS TIPO_DCTO_CONTRATISTA
 ON CONTRATISTA.COMPANIA = TIPO_DCTO_CONTRATISTA.COMPANIA
AND CONTRATISTA.TIPOID = TIPO_DCTO_CONTRATISTA.DCTO_IDENTIDAD
LEFT JOIN TERCERO INTERVENTOR
 ON ORDENDECOMPRA.COMPANIA = INTERVENTOR.COMPANIA
AND ORDENDECOMPRA.CEDULAINTERVENTOR = INTERVENTOR.NIT
AND ORDENDECOMPRA.SUCURSALINTERVENTOR = INTERVENTOR.SUCURSAL
LEFT JOIN TIPOS_DOCUMENTOS TIPO_DCTO_INTERVENTOR
 ON INTERVENTOR.COMPANIA = TIPO_DCTO_INTERVENTOR.COMPANIA
AND INTERVENTOR.TIPOID = TIPO_DCTO_INTERVENTOR.DCTO_IDENTIDAD
INNER JOIN COMPANIA
ON ORDENDECOMPRA.COMPANIA = COMPANIA.CODIGO
LEFT JOIN ORDENDECOMPRA MODIFICACIONES
ON ORDENDECOMPRA.COMPANIA = MODIFICACIONES.COMPANIA
AND ORDENDECOMPRA.CLASEORDEN = MODIFICACIONES.TIPOAFECTADO
AND ORDENDECOMPRA.NUMERO = MODIFICACIONES.NUMEROAFECTADO;
COMMIT;