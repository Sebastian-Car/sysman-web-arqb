CREATE OR REPLACE VIEW V_ORDENDECOMPRA_FTERECURSO AS 
WITH UNICOS AS
(
SELECT DISTINCT
  ORDENDECOMPRAPPTO.COMPANIA, 
  ORDENDECOMPRAPPTO.CLASEORDEN,
  ORDENDECOMPRAPPTO.NUMERO,  
  DECODE(ORDENDECOMPRAPPTO.FUENTE_RECURSO,PCK_DATOS.FC_CONS_FUENTE,'',ORDENDECOMPRAPPTO.FUENTE_RECURSO) FUENTERECURSO,
  DECODE(ORDENDECOMPRAPPTO.FUENTE_RECURSO,PCK_DATOS.FC_CONS_FUENTE,'',FUENTE_RECURSOS.NOMBRE )   FTENOM
FROM ORDENDECOMPRAPPTO 
INNER JOIN FUENTE_RECURSOS   
 ON ORDENDECOMPRAPPTO.COMPANIA = FUENTE_RECURSOS.COMPANIA
AND ORDENDECOMPRAPPTO.ANOPPTO  = FUENTE_RECURSOS.ANO
AND ORDENDECOMPRAPPTO.FUENTE_RECURSO    = FUENTE_RECURSOS.CODIGO
)
SELECT 
  COMPANIA, 
  CLASEORDEN,
  NUMERO,
  LISTAGG(FUENTERECURSO , ',') WITHIN GROUP(ORDER BY COMPANIA ,CLASEORDEN , NUMERO ) FUENTE_RECURSO,
  LISTAGG(FTENOM , ',') WITHIN GROUP(ORDER BY COMPANIA ,CLASEORDEN , NUMERO ) FTENOM
FROM UNICOS 
GROUP BY COMPANIA, 
  CLASEORDEN,
  NUMERO WITH READ ONLY
 
  
 ;