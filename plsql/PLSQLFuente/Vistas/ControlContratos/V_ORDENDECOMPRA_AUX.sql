CREATE OR REPLACE VIEW V_ORDENDECOMPRA_AUX AS 
WITH UNICOS AS
(
SELECT DISTINCT
  ORDENDECOMPRAPPTO.COMPANIA, 
  ORDENDECOMPRAPPTO.CLASEORDEN,
  ORDENDECOMPRAPPTO.NUMERO,  
  DECODE(ORDENDECOMPRAPPTO.AUXILIAR,PCK_DATOS.FC_CONS_AUXILIAR,'',ORDENDECOMPRAPPTO.AUXILIAR) AUXILIAR,
  DECODE(ORDENDECOMPRAPPTO.AUXILIAR,PCK_DATOS.FC_CONS_AUXILIAR,'',AUXILIAR.NOMBRE )   AUXNOM
FROM ORDENDECOMPRAPPTO 
INNER JOIN AUXILIAR   
 ON ORDENDECOMPRAPPTO.COMPANIA = AUXILIAR.COMPANIA
AND ORDENDECOMPRAPPTO.ANOPPTO  = AUXILIAR.ANO
AND ORDENDECOMPRAPPTO.AUXILIAR = AUXILIAR.CODIGO
)
SELECT 
  COMPANIA, 
  CLASEORDEN,
  NUMERO,
  LISTAGG(AUXILIAR , ',') WITHIN GROUP(ORDER BY COMPANIA ,CLASEORDEN , NUMERO ) AUXILIAR,
  LISTAGG(AUXNOM , ',') WITHIN GROUP(ORDER BY COMPANIA ,CLASEORDEN , NUMERO ) AUXNOM
FROM UNICOS 
GROUP BY COMPANIA, 
  CLASEORDEN,
  NUMERO WITH READ ONLY
 
  
;
