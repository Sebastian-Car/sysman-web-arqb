CREATE OR REPLACE VIEW V_ORDENDECOMPRA_DIS AS 
WITH UNICOS AS
(
  SELECT DISTINCT
    ORDENDECOMPRAPPTO.COMPANIA, 
    ORDENDECOMPRAPPTO.CLASEORDEN,
    ORDENDECOMPRAPPTO.NUMERO,  
    COMPROBANTE_PPTAL.TIPO,
    COMPROBANTE_PPTAL.ANO,
    COMPROBANTE_PPTAL.NUMERO COMPROBANTE,
    TO_CHAR(COMPROBANTE_PPTAL.FECHA,'DD/MM/YYYY') FECHA,
    COMPROBANTE_PPTAL.DEBITO,
    TIPO_COMPROBPP.CLASE
  FROM COMPROBANTE_PPTAL 
  INNER JOIN ORDENDECOMPRAPPTO 
    ON  ORDENDECOMPRAPPTO.COMPANIA    = COMPROBANTE_PPTAL.COMPANIA       
    AND ORDENDECOMPRAPPTO.ANOPPTO     = COMPROBANTE_PPTAL.ANO       
    AND ORDENDECOMPRAPPTO.TIPOPPTO    = COMPROBANTE_PPTAL.TIPO     
    AND ORDENDECOMPRAPPTO.NUMEROPPTO  = COMPROBANTE_PPTAL.NUMERO
  INNER JOIN TIPO_COMPROBPP
  ON COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA
  AND COMPROBANTE_PPTAL.TIPO =  TIPO_COMPROBPP.CODIGO
)
SELECT 
  COMPANIA, 
  CLASEORDEN,
  NUMERO,  
  TIPO,
  ANO,
  LISTAGG(FECHA , ',') WITHIN GROUP(ORDER BY COMPANIA ,NUMERO , TIPO ) FECHACDP,
  LISTAGG(COMPROBANTE , ',') WITHIN GROUP(ORDER BY COMPANIA ,NUMERO , TIPO ) COMPROBANTES,
  SUM(DEBITO) TOTAL_DIS
FROM UNICOS 
WHERE CLASE = 'DIS' 
GROUP BY COMPANIA, 
  CLASEORDEN,
  NUMERO,  
  TIPO,
  ANO
   
 ;