CREATE OR REPLACE VIEW V_VOLANTES AS 
WITH
DEDUC AS (
  SELECT COMPANIA, ID_DE_PROCESO, ANO, MES, PERIODO, ID_DE_EMPLEADO, 
         SUM(VALOR) DEDUCIBLE
  FROM NOVEDADES
  WHERE ID_DE_CONCEPTO  IN(300,301)
        AND ID_DE_PROCESO=0
        AND ANO=0
        AND MES=0
        AND PERIODO=0
  GROUP BY COMPANIA, ID_DE_PROCESO, ANO, MES, PERIODO, ID_DE_EMPLEADO
),
CATEGO AS (
  SELECT COMPANIA, ESCALAFON, ID_DE_CATEGORIA, ANO, SALARIO_BASE, CODIGOGRADO, VLR_INCREMENTO
  FROM CATEGORIA
)
,
SALARIOBASEH AS (
  SELECT COMPANIA, ID_DE_PROCESO, ANO, MES, PERIODO, ID_DE_EMPLEADO, VALOR
  FROM HISTORICOS
  WHERE ID_DE_CONCEPTO=1
),
SALARIOENCARGOH AS (
  SELECT COMPANIA, ID_DE_PROCESO, ANO, MES, PERIODO, ID_DE_EMPLEADO, VALOR
  FROM HISTORICOS
  WHERE ID_DE_CONCEPTO=10
),
ENCARGO AS (SELECT HISTORICOS.COMPANIA,
                   HISTORICOS.ID_DE_PROCESO,
                   HISTORICOS.ANO,
                   HISTORICOS.MES,
                   HISTORICOS.PERIODO,
                   HISTORICOS.ID_DE_EMPLEADO,
                   MIN(CARGOS.NOMBRE_DEL_CARGO) NOMBRE_DEL_CARGO,
           ENCARGOS.FECHAINICIO,
                   ENCARGOS.FECHAFINAL
            FROM ENCARGOS 
            INNER JOIN CARGOS
               ON ENCARGOS.COMPANIA       = CARGOS.COMPANIA
              AND ENCARGOS.ID_DE_CARGO    = CARGOS.ID_DE_CARGO
            INNER JOIN HISTORICOS  
               ON ENCARGOS.COMPANIA       = HISTORICOS.COMPANIA
              AND ENCARGOS.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
              AND ENCARGOS.ID_DE_PROCESO  = HISTORICOS.ID_DE_PROCESO
              AND ENCARGOS.FECHAINICIO   <= LAST_DAY(TO_DATE('01/' || HISTORICOS.MES || '/' || HISTORICOS.ANO, 'DD/MM/YYYY'))
              AND ENCARGOS.FECHAFINAL    >= TO_DATE('01/' || HISTORICOS.MES || '/' || HISTORICOS.ANO, 'DD/MM/YYYY')
            INNER JOIN CONCEPTOS 
                 ON CONCEPTOS.COMPANIA        = HISTORICOS.COMPANIA
                 AND CONCEPTOS.ID_DE_CONCEPTO = HISTORICOS.ID_DE_CONCEPTO
            WHERE CONCEPTOS.CLASE IN(3,5)
            GROUP BY HISTORICOS.COMPANIA,
                     HISTORICOS.ID_DE_PROCESO,
                     HISTORICOS.ANO,
                     HISTORICOS.MES,
                     HISTORICOS.PERIODO,
                     HISTORICOS.ID_DE_EMPLEADO,
           ENCARGOS.FECHAINICIO,
                     ENCARGOS.FECHAFINAL
    )
SELECT 
  HIS.COMPANIA,
  HIS.ID_DE_PROCESO,
  HIS.ANO,
  HIS.MES,
  HIS.PERIODO,
  HIS.ID_DE_EMPLEADO,
  HISTORICOS.ID_DE_CONCEPTO,
  CONCEPTOS.NOMBRE_CONCEPTO,
  CONCEPTOS.CLASE,
  CASE
    WHEN CONCEPTOS.CLASE IN(3)
    THEN 'Remuneracion'
  ELSE
    'Descuento'
  END CLASE_NOMBRE,
  HIS.NOMBRECOMPLETO,
  HISTORICOS.FECHA,
  HIS.ID_CENTRO_DE_COSTO,
  HIS.NOMBRE_CENTRO_DE_COSTO,
  HIS.CUENTA,
  HISTORICOS.VALOR,
  CASE
    WHEN CONCEPTOS.CLASE IN(3)
    THEN HISTORICOS.VALOR
  ELSE
    0
  END DEVENGOS,
  CASE
    WHEN CONCEPTOS.CLASE IN(5)
    THEN HISTORICOS.VALOR
  ELSE
    0
  END DESCUENTOS,
  CASE 
  WHEN CONCEPTOS.CLASE IN(3)
  THEN HISTORICOS.VALOR
  ELSE
    CASE
    WHEN CONCEPTOS.CLASE IN(5)
    THEN HISTORICOS.VALOR*-1
    ELSE
    0
    END
  END  NETOAPAGAR,
  HIS.DEPENDENCIA,
  HIS.NOMBRE_DEPENDENCIA,
  HIS.BANCO,
  HIS.NOMBRE_BANCO,
  HIS.NOMBRE_DE_CARGO,
  HIS.ID_DE_CARGO,
  HIS.NUMERO_DCTO,
  HIS.FECHA_DE_INGRESO,
  HIS.NOMBRE_FONDOSALUD,
  HIS.NOMBRE_FONDOPENSION,
  HIS.NOMBRE_FONDORIESGOS,
  HIS.NOMBRE_FONDOPENSION_VOL,
  HIS.NOMBRE_FONDOCESANTIAS,
  HIS.NOMBRE_CAJACOMPENSACION,
  HIS.NOMBRE_MEDICINAPREPAGADA,
  HIS.SALARIO_BASE_IBC SALARIO_BASE,
  SALARIOBASEH.VALOR SALARIOBASEHISTORICO,
  SALARIOENCARGOH.VALOR SALARIOENCARGOHISTORICO,
  PERIODOS.FECHAINICIO, 
  PERIODOS.FECHAFINAL,
  PERIODOS.NOM_PERIODO,
  DEDUC.DEDUCIBLE ,
  CATEGO.SALARIO_BASE SALARIO_BASECATE,
  CATEGO.CODIGOGRADO,
  CATEGO.VLR_INCREMENTO,
  CATEGO.ESCALAFON,
  CATEGO.ID_DE_CATEGORIA,
  (
   SELECT HISTORICOSREL.VALOR
   FROM HISTORICOS HISTORICOSREL
   WHERE CONCEPTOS.COMPANIA        = HISTORICOSREL.COMPANIA
      AND CONCEPTOS.RELACIONADO    = HISTORICOSREL.ID_DE_CONCEPTO
      AND HISTORICOSREL.COMPANIA       = HISTORICOS.COMPANIA
      AND HISTORICOSREL.ID_DE_PROCESO  = HISTORICOS.ID_DE_PROCESO
      AND HISTORICOSREL.MES            = HISTORICOS.MES
      AND HISTORICOSREL.ANO            = HISTORICOS.ANO
      AND HISTORICOSREL.PERIODO        = HISTORICOS.PERIODO
      AND HISTORICOSREL.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
   ) CANTIDAD,
   ENCARGO.NOMBRE_DEL_CARGO NOMBRE_DEL_CARGO_ENCARGO,
   ENCARGO.FECHAINICIO FECHAINICIO_ENCARGO,
   ENCARGO.FECHAFINAL FECHAFINAL_ENCARGO
FROM PERSONAL_HISTORICO HIS
INNER JOIN HISTORICOS
      ON HIS.COMPANIA        = HISTORICOS.COMPANIA
      AND HIS.ID_DE_PROCESO  = HISTORICOS.ID_DE_PROCESO
      AND HIS.MES            = HISTORICOS.MES
      AND HIS.ANO            = HISTORICOS.ANO
      AND HIS.PERIODO        = HISTORICOS.PERIODO
      AND HIS.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
INNER JOIN CONCEPTOS 
     ON CONCEPTOS.COMPANIA        = HISTORICOS.COMPANIA
     AND CONCEPTOS.ID_DE_CONCEPTO = HISTORICOS.ID_DE_CONCEPTO
INNER JOIN PERIODOS
        ON HISTORICOS.COMPANIA        = PERIODOS.COMPANIA
        AND HISTORICOS.ID_DE_PROCESO  = PERIODOS.ID_DE_PROCESO
        AND HISTORICOS.MES            = PERIODOS.MES
        AND HISTORICOS.ANO            = PERIODOS.ANO
        AND HISTORICOS.PERIODO        = PERIODOS.PERIODO
LEFT JOIN DEDUC 
        ON DEDUC.COMPANIA       = HIS.COMPANIA
       AND DEDUC.ID_DE_PROCESO  = 0
       AND DEDUC.ANO            = 0
       AND DEDUC.MES            = 0
       AND DEDUC.PERIODO        = 0
       AND DEDUC.ID_DE_EMPLEADO = HIS.ID_DE_EMPLEADO
LEFT JOIN  CATEGO
         ON CATEGO.COMPANIA       = HIS.COMPANIA
        AND CATEGO.ESCALAFON      = HIS.ESCALAFON
        AND CATEGO.ID_DE_CATEGORIA= HIS.ID_DE_CATEGORIA
        AND CATEGO.ANO            = HIS.ANO 
LEFT JOIN SALARIOBASEH
         ON SALARIOBASEH.COMPANIA       = HISTORICOS.COMPANIA
        AND SALARIOBASEH.ID_DE_PROCESO  = HISTORICOS.ID_DE_PROCESO
        AND SALARIOBASEH.MES            = HISTORICOS.MES
        AND SALARIOBASEH.ANO            = HISTORICOS.ANO
        AND SALARIOBASEH.PERIODO        = HISTORICOS.PERIODO
        AND SALARIOBASEH.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
LEFT JOIN SALARIOENCARGOH
         ON SALARIOENCARGOH.COMPANIA       = HISTORICOS.COMPANIA
        AND SALARIOENCARGOH.ID_DE_PROCESO  = HISTORICOS.ID_DE_PROCESO
        AND SALARIOENCARGOH.MES            = HISTORICOS.MES
        AND SALARIOENCARGOH.ANO            = HISTORICOS.ANO
        AND SALARIOENCARGOH.PERIODO        = HISTORICOS.PERIODO
        AND SALARIOENCARGOH.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO              
LEFT JOIN ENCARGO 
  ON  ENCARGO.COMPANIA      = HISTORICOS.COMPANIA
  AND ENCARGO.ID_DE_PROCESO = HISTORICOS.ID_DE_PROCESO
  AND ENCARGO.ANO           = HISTORICOS.ANO
  AND ENCARGO.MES           = HISTORICOS.MES
  AND ENCARGO.PERIODO       = HISTORICOS.PERIODO
  AND ENCARGO.ID_DE_EMPLEADO= HISTORICOS.ID_DE_EMPLEADO
WHERE CONCEPTOS.CLASE IN(3,5) WITH READ ONLY;

   COMMENT ON COLUMN "V_VOLANTES"."COMPANIA" IS 'Codigo de la compa?ia a la que pertenece el registro';
   COMMENT ON COLUMN "V_VOLANTES"."ID_DE_PROCESO" IS 'Identifica el proceso de calculo de nomina';
   COMMENT ON COLUMN "V_VOLANTES"."ANO" IS 'A?o al que pertenece el registro';
   COMMENT ON COLUMN "V_VOLANTES"."MES" IS 'identificar numerico del mes al cual pertenece el registro';
   COMMENT ON COLUMN "V_VOLANTES"."PERIODO" IS 'Identifica el periodo asociado en el registro';
   COMMENT ON COLUMN "V_VOLANTES"."ID_DE_EMPLEADO" IS 'Identifica el codigo del empleado';
   COMMENT ON COLUMN "V_VOLANTES"."ID_CENTRO_DE_COSTO" IS 'Identificador del centro de costo que se asocia en el registro';
   COMMENT ON COLUMN "V_VOLANTES"."DEPENDENCIA" IS 'identificar de la dependencia relacionada al registro';
   COMMENT ON COLUMN "V_VOLANTES"."ID_DE_CARGO" IS 'Identificador del cargo que se asocia en el registro';
   COMMENT ON COLUMN "V_VOLANTES"."NUMERO_DCTO" IS 'Identificacion del empleado o tercero ';
   COMMENT ON COLUMN "V_VOLANTES"."FECHA_DE_INGRESO" IS 'Fecha de ingreso de un empleado';
   COMMENT ON COLUMN "V_VOLANTES"."NOM_PERIODO" IS 'Identifica el nombre del periodo asociado en el registro';
   COMMENT ON COLUMN "V_VOLANTES"."ID_DE_CATEGORIA" IS 'Identificador de la categoria del empleado que se asocia en el registro';
