CREATE OR REPLACE VIEW V_TARJETAKARDEX AS 
SELECT  INVENTARIO.COMPANIA, 
        INVENTARIO.CODIGOELEMENTO, 
        INVENTARIO.TIPO,
        INVENTARIO.TIENEMOVIMIENTO,
        INVENTARIO.VLRUNITARIOPROM,
        D_MOVIMIENTO.FECHA , 
        D_MOVIMIENTO.HORA, 
        D_MOVIMIENTO.SERIE, 
        D_MOVIMIENTO.TIPOMOVIMIENTO, 
        D_MOVIMIENTO.MOVIMIENTO, 
        D_MOVIMIENTO.CODIGO,
        D_MOVIMIENTO.CANTIDAD,
        D_MOVIMIENTO.VLRUNITARIOPROM AS VLRPROMEDIADO,
        D_MOVIMIENTO.VALORUNITARIO AS VLRMOV, 
        D_MOVIMIENTO.VALORSALDO AS VLRSALDO,
        D_MOVIMIENTO.VALORTOTAL,
        D_MOVIMIENTO.VLRAJUSTADO,
        D_MOVIMIENTO.VLRAJUSTADO * D_MOVIMIENTO.SALDOKARDEX AS VLRSALDOAJUSTADO, 
        D_MOVIMIENTO.ESTADO,
        V_MOVIMIENTO.CLASE,
        V_MOVIMIENTO.BODEGA_ORIGEN,
        V_MOVIMIENTO.BODEGA_DESTINO,
        D_MOVIMIENTO.SALDOKARDEX AS SSALDOKARDEX,
        NVL(D_MOVIMIENTO.VALORSALDO ,INVENTARIO.VALORTOTAL) AS VALORSALDOS, 
        DECODE(V_MOVIMIENTO.CLASE,'E',CANTIDAD,0) AS ENTRADAS, 
        DECODE(V_MOVIMIENTO.CLASE,'S',CANTIDAD,0) AS SALIDAS, 
        DECODE(V_MOVIMIENTO.CLASE,'E',D_MOVIMIENTO.VALORTOTAL,0) AS VLRENTRADAS,    
        DECODE(V_MOVIMIENTO.CLASE,'S',D_MOVIMIENTO.VLRUNITARIOPROM * CANTIDAD,0) AS VLRSALIDAS_PROM,
        DECODE(V_MOVIMIENTO.CLASE,'S',D_MOVIMIENTO.VALORTOTAL,0) AS VLRSALIDAS,
        NVL(D_MOVIMIENTO.SALDOKARDEX, INVENTARIO.EXISTENCIA) AS SALDOKARDEX, 
        CASE WHEN 
          CASE WHEN V_MOVIMIENTO.CLASE ='E'
            THEN D_MOVIMIENTO.VALORTOTAL
            ELSE 0
            END >0 
        THEN NVL(D_MOVIMIENTO.VALORSALDO ,INVENTARIO.VALORTOTAL) +  
          CASE WHEN V_MOVIMIENTO.CLASE ='E'
            THEN D_MOVIMIENTO.VALORTOTAL
            ELSE 0
            END 
        ELSE    
          CASE WHEN V_MOVIMIENTO.CLASE = 'S'
            THEN D_MOVIMIENTO.VLRUNITARIOPROM * CANTIDAD
            ELSE 0
            END - NVL(D_MOVIMIENTO.VALORSALDO ,INVENTARIO.VALORTOTAL) END AS VLRTOTAL_PEPS, 
        D_MOVIMIENTO.SALDOKARDEX * D_MOVIMIENTO.VLRUNITARIOPROM AS VLRTOTAL,
        DEPENDENCIA.NOMBRE AS NOMBREDEPENDENCIA, 
        TERCERO.NOMBRE AS NOMBRETERCERO, 
        INVENTARIO.NOMBRELARGO, 
        INVENTARIO.UNIDAD
FROM INVENTARIO 
  INNER JOIN D_MOVIMIENTO
     ON INVENTARIO.COMPANIA = D_MOVIMIENTO.COMPANIA 
     AND INVENTARIO.CODIGOELEMENTO = D_MOVIMIENTO.ELEMENTO 
    INNER JOIN V_MOVIMIENTO 
      ON D_MOVIMIENTO.COMPANIA = V_MOVIMIENTO.COMPANIA 
      AND D_MOVIMIENTO.TIPOMOVIMIENTO = V_MOVIMIENTO.TIPOMOVIMIENTO 
      AND D_MOVIMIENTO.MOVIMIENTO = V_MOVIMIENTO.NUMERO 
    INNER JOIN TERCERO 
      ON V_MOVIMIENTO.COMPANIA = TERCERO.COMPANIA
      AND V_MOVIMIENTO.TERCERO = TERCERO.NIT 
      AND  V_MOVIMIENTO.SUCURSAL = TERCERO.SUCURSAL 
    INNER JOIN DEPENDENCIA 
      ON V_MOVIMIENTO.COMPANIA = DEPENDENCIA.COMPANIA 
      AND V_MOVIMIENTO.DEPENDENCIA_DESTINO = DEPENDENCIA.CODIGO
 ;