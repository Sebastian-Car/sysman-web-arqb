CREATE OR REPLACE FORCE EDITIONABLE VIEW "V_FACTORESVACACIONES_SINCHI" ("COMPANIA", "ID_DE_PROCESO", "ANO", "MES", "ID_DE_EMPLEADO", "NOMBRECOMPLETO", "NOMBRE_DEL_CARGO", "NUMERO_DCTO", "FECHA_DE_INGRESO", "FECHA_INICIO", "FECHA_FINAL", "INICIO_DISFRUTE", "FINAL_DISFRUTE", "NOMBRE", "NOMBRECIUDAD", "ANTIGUEDAD", "FACTOR2", "FACTOR3", "FACTOR7", "FACTOR8", "FACTOR9", "FACTOR10", "FACTOR11", "FACTOR12", "FACTOR13", "NUMPER", "DIASVAC", "DIASPRIMA", "DIASCAL", "DIASDINERO", "BONRECRE", "PRIMVAC", "DIASHAB", "PANTIGUEDAD", "VACDINERO", "EXTRASPV", "SALVAC", "TOTALFACTOR", "TOTALPAGO", "FECHA", "DIASRECRE", "ACTO", "FECHA_ACTO", "DEPENDENCIA") AS 
  SELECT
  HISTORICOS.COMPANIA,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.ID_DE_EMPLEADO,
  PERSONAL_HISTORICO.NOMBRECOMPLETO,
  PERSONAL_HISTORICO.NOMBRE_DE_CARGO NOMBRE_DEL_CARGO ,
  PERSONAL_HISTORICO.NUMERO_DCTO,
  PERSONAL_HISTORICO.FECHA_DE_INGRESO,
  VACACIONES.FECHA_INICIO,
  VACACIONES.FECHA_FINAL,
  VACACIONES.INICIO_DISFRUTE,
  VACACIONES.FINAL_DISFRUTE,
  PERSONAL_HISTORICO.NOMBRE_DEPENDENCIA NOMBRE,
  PERSONAL_HISTORICO.NOMBRE_CIUDAD_HAB AS NOMBRECIUDAD,
  ROUND((PCK_SYSMAN_UTL.FC_EDAD(PERSONAL_HISTORICO.FECHA_DE_INGRESO,VACACIONES.INICIO_DISFRUTE)/365),2) ANTIGUEDAD,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 977 THEN HISTORICOS.VALOR ELSE 0 END) FACTOR2,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 980 THEN HISTORICOS.VALOR ELSE 0 END) FACTOR3,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 978 THEN HISTORICOS.VALOR ELSE 0 END) FACTOR7,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 979 THEN HISTORICOS.VALOR ELSE 0 END) FACTOR8,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 981 THEN HISTORICOS.VALOR ELSE 0 END) FACTOR9,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 975 THEN HISTORICOS.VALOR ELSE 0 END) FACTOR10,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 982 THEN HISTORICOS.VALOR ELSE 0 END) FACTOR11,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 984 THEN HISTORICOS.VALOR ELSE 0 END) FACTOR12,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 999 THEN HISTORICOS.VALOR ELSE 0 END) FACTOR13,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 164 THEN HISTORICOS.VALOR ELSE 0 END) NUMPER,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO =  35 THEN HISTORICOS.VALOR ELSE 0 END) DIASVAC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO =  93 THEN HISTORICOS.VALOR ELSE 0 END) DIASPRIMA,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO =  94 THEN HISTORICOS.VALOR ELSE 0 END) DIASCAL,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO =  96 THEN HISTORICOS.VALOR ELSE 0 END) DIASDINERO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 151 THEN HISTORICOS.VALOR ELSE 0 END) BONRECRE,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 155 THEN HISTORICOS.VALOR ELSE 0 END) PRIMVAC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 162 THEN HISTORICOS.VALOR ELSE 0 END) DIASHAB,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 170 THEN HISTORICOS.VALOR ELSE 0 END) PANTIGUEDAD,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 175 THEN HISTORICOS.VALOR ELSE 0 END) VACDINERO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 976 THEN HISTORICOS.VALOR ELSE 0 END) EXTRASPV,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 174 THEN HISTORICOS.VALOR ELSE 0 END)  SALVAC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN(977, 980, 978, 979, 981, 975, 982, 984, 170, 976,999) THEN HISTORICOS.VALOR ELSE 0 END) TOTALFACTOR,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN(151,175,155) THEN HISTORICOS.VALOR ELSE 0 END)
  + SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 174 THEN HISTORICOS.VALOR ELSE 0 END) TOTALPAGO,
  MAX(HISTORICOS.FECHA) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) FECHA,
  MAX(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 151 AND HISTORICOS.VALOR <> 0 THEN TO_NUMBER(PCK_SYSMAN_UTL.FC_PAR(HISTORICOS.COMPANIA,'NUMERO DE DIAS PARA BONIFICACION DE RECREACION', 6, TO_DATE('01/' || HISTORICOS.MES || '/' || HISTORICOS.ANO,'DD/MM/YYYY'))) ELSE 0 END) DIASRECRE,
  VACACIONES.ACTO,
  VACACIONES.FECHA_ACTO,
  PERSONAL_HISTORICO.DEPENDENCIA
FROM HISTORICOS INNER JOIN PERIODOS
  ON HISTORICOS.COMPANIA      = PERIODOS.COMPANIA
 AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO
 AND HISTORICOS.ANO           = PERIODOS.ANO
 AND HISTORICOS.MES           = PERIODOS.MES
 AND HISTORICOS.PERIODO       = PERIODOS.PERIODO
INNER JOIN VACACIONES
  ON HISTORICOS.COMPANIA      = VACACIONES.COMPANIA
 AND HISTORICOS.ID_DE_PROCESO = VACACIONES.ID_DE_PROCESO
 AND HISTORICOS.ANO           = VACACIONES.ANO
 AND HISTORICOS.MES           = VACACIONES.MES
 AND HISTORICOS.PERIODO IN (CASE WHEN NVL(PCK_SYSMAN_UTL.FC_PAR(HISTORICOS.COMPANIA, 'LIQUIDAN VACACIONES DENTRO DE NOMINA',6,SYSDATE),'SI') = 'NO' THEN TO_NUMBER(NVL(PCK_SYSMAN_UTL.FC_PAR(HISTORICOS.COMPANIA, 'PERIODO NOMINA ADICIONAL',6,SYSDATE),'0')) ELSE 0 END, VACACIONES.PERIODO)
 AND HISTORICOS.ID_DE_EMPLEADO= VACACIONES.ID_DE_EMPLEADO
INNER JOIN PERSONAL_HISTORICO
  ON HISTORICOS.COMPANIA      = PERSONAL_HISTORICO.COMPANIA
 AND HISTORICOS.ID_DE_PROCESO = PERSONAL_HISTORICO.ID_DE_PROCESO
 AND HISTORICOS.ANO           = PERSONAL_HISTORICO.ANO
 AND HISTORICOS.MES           = PERSONAL_HISTORICO.MES
 AND HISTORICOS.PERIODO       = PERSONAL_HISTORICO.PERIODO
 AND HISTORICOS.ID_DE_EMPLEADO= PERSONAL_HISTORICO.ID_DE_EMPLEADO
WHERE PERIODOS.ACUMULADO NOT IN(0)
  AND (HISTORICOS.ID_DE_CONCEPTO BETWEEN 800 AND 819
   OR  HISTORICOS.ID_DE_CONCEPTO BETWEEN 975 AND 982
   OR  HISTORICOS.ID_DE_CONCEPTO IN(1, 35, 61, 80, 93, 94, 96, 151, 155, 162, 164, 170, 174, 175, 547, 984,999))
GROUP BY HISTORICOS.COMPANIA,
        HISTORICOS.ID_DE_PROCESO,
        HISTORICOS.ANO,
        HISTORICOS.MES,
        HISTORICOS.ID_DE_EMPLEADO,
        PERSONAL_HISTORICO.NOMBRECOMPLETO,
        PERSONAL_HISTORICO.NOMBRE_DE_CARGO,
        PERSONAL_HISTORICO.NUMERO_DCTO,
        PERSONAL_HISTORICO.FECHA_DE_INGRESO,
        VACACIONES.FECHA_INICIO,
        VACACIONES.FECHA_FINAL,
        VACACIONES.INICIO_DISFRUTE,
        VACACIONES.FINAL_DISFRUTE,
        PERSONAL_HISTORICO.NOMBRE_DEPENDENCIA,
        PERSONAL_HISTORICO.NOMBRE_CIUDAD_HAB,
        VACACIONES.ACTO,
        VACACIONES.FECHA_ACTO,
        PERSONAL_HISTORICO.DEPENDENCIA
HAVING SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN(155,174,175) THEN HISTORICOS.VALOR ELSE 0 END) NOT IN(0)
ORDER BY HISTORICOS.COMPANIA, HISTORICOS.ID_DE_PROCESO, HISTORICOS.ANO, HISTORICOS.MES, HISTORICOS.ID_DE_EMPLEADO;