CREATE OR REPLACE VIEW V_DIAN AS 
SELECT HISTORICOS.COMPANIA
     ,PCK_SYSMAN_UTL.FC_MINIT(HISTORICOS.COMPANIA,312) NIT_RETENEDOR_ESPACIOS 
     ,PCK_SYSMAN_UTL.FC_MINIT(HISTORICOS.COMPANIA,31)  NIT_RETENEDOR 
     ,PCK_SYSMAN_UTL.FC_MINIT(HISTORICOS.COMPANIA,1) DV_RETENEDOR
     ,TIPOS_DOCUMENTOS.CODIGODIAN TIPODCTO
     ,TERCERO.NIT NUMERO_DOCUMENTO
     ,COMPANIA.NOMBRE RAZON_SOCIAL_RETENEDOR_COM
     ,TER_COMPANIA.NOMBRE  RAZON_SOCIAL_RETENEDOR
     ,TERCERO.APELLIDO1
     ,TERCERO.APELLIDO2
     ,TERCERO.NOMBRE1
     ,TERCERO.NOMBRE2
     ,PERSONAL.REGIMEN
     ,(TRIM(TERCERO.NOMBRE1)  || ' ' ||TRIM(TERCERO.NOMBRE2)) NOMBRES
     ,TERCERO.DIRECCION
     ,HISTORICOS.ANO
     ,CIUDAD.CODIGODANE CODIGOCIUDAD
     ,CIUDAD.NOMBRE NOMBRECIUDAD
     ,DEPARTAMENTO.CODIGODANE CODIGODEPARTAMENTO
     ,ANO.VALORUVT
     ,ANO.PATRIMONIOBRUTO_UVT
     ,ROUND(ANO.VALORUVT * ANO.PATRIMONIOBRUTO_UVT,-3) PATRIMONIOBRUTO_VALOR
     ,ANO.INGRESOSTOTALES_UVT
     ,ROUND(ANO.VALORUVT * ANO.INGRESOSTOTALES_UVT,-3) INGRESOSTOTALES_VALOR
     ,ANO.CONSUMOTARJETACRE_UVT
     ,ROUND(ANO.VALORUVT * ANO.CONSUMOTARJETACRE_UVT,-3) CONSUMOTARJETACRE_VALOR
     ,ANO.COMPRASYCONSUMO_UVT
     ,ROUND(ANO.VALORUVT * ANO.COMPRASYCONSUMO_UVT,-3) COMPRASYCONSUMO_VALOR
     ,ANO.CONSIGNACIONESBANCARIAS_UVT
     ,ROUND(ANO.VALORUVT * ANO.CONSIGNACIONESBANCARIAS_UVT,-3) CONSIGNACIONESBANCARIAS_VALOR
     ,TIPDOC2.CODIGODIAN TIPODCTODEPEN
	 ,PERSONAL.NUMERO_IDEN_DEPEN
	 ,PERSONAL.NOMBRES_DEPEN
	 ,PARENTESCO.DESCRIPCION PARENTESCO_DEPEN
     ,MIN(CASE
        WHEN TRUNC(PERSONAL.FECHA_DE_INGRESO) BETWEEN TO_DATE('01/01/' || HISTORICOS.ANO, 'DD/MM/YYYY') AND TO_DATE('31/12/' || HISTORICOS.ANO, 'DD/MM/YYYY')
        THEN PERSONAL.FECHA_DE_INGRESO
        ELSE TO_DATE('01/01/' || HISTORICOS.ANO, 'DD/MM/YYYY')
      END) FECHAINICIAL 
     ,MAX(CASE
        WHEN TRUNC(PERSONAL.FECHA_DE_RETIRO -1) BETWEEN TO_DATE('01/01/' || HISTORICOS.ANO, 'DD/MM/YYYY') AND TO_DATE('31/12/' || HISTORICOS.ANO, 'DD/MM/YYYY')
        THEN PERSONAL.FECHA_DE_RETIRO -1
        ELSE TO_DATE('31/12/' || HISTORICOS.ANO, 'DD/MM/YYYY')
      END) FECHAFINAL
     ,PCK_SYSMAN_UTL.FC_ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN ='31' AND CONCEPTOS.CLASE  IN (3,5,8) 
          THEN HISTORICOS.VALOR * CASE WHEN CONCEPTOS.CLASE = 5 THEN -1 ELSE 1 END
          ELSE 0 END),0),-3)  AS S31                         
     ,PCK_SYSMAN_UTL.FC_ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN ='32'
          THEN HISTORICOS.VALOR 
          ELSE 0 
      END),0),-3) AS S32  
    ,PCK_SYSMAN_UTL.FC_ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN ='33'AND CONCEPTOS.CLASE = 3 
       THEN HISTORICOS.VALOR 
       ELSE 0 
       END) +
       SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN ='33'AND CONCEPTOS.CLASE = 5 
           THEN HISTORICOS.VALOR * (-1) 
           ELSE 0 
       END),0),-3) AS S33
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='30' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S30           
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='34' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S34  
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='35' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S35
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='36' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S36    
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='37' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S37  
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='38' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S38  
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='39' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S39 
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='40' THEN CASE WHEN CONCEPTOS.CLASE = 5 THEN HISTORICOS.VALOR ELSE HISTORICOS.VALOR *(-1) END
                                        ELSE 0 END),0),-3) AS RETE
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='40' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S40 
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='41' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S41  
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='42' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S42  
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='43' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S43  
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='44' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S44  
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='45' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S45  
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='46' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S46  
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='47' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S47  
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='48' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S48
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='49' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S49  
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='50' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S50
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='51' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S51
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='52' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S52
    ,ROUND(NVL(SUM(DECODE(PERSONAL.REGIMEN,1,DECODE(CONCEPTOS_ANO.CODDIAN,'32',HISTORICOS.VALOR,0),0)),0),-3) AS S32_1
    ,ROUND(NVL(SUM(DECODE(PERSONAL.REGIMEN,1,0,DECODE(CONCEPTOS_ANO.CODDIAN,'32',HISTORICOS.VALOR,0))),0),-3) AS S32_2
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='53' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S53
    ,ROUND(NVL(SUM(CASE WHEN CONCEPTOS_ANO.CODDIAN='54' THEN HISTORICOS.VALOR ELSE 0 END),0),-3) AS S54
    ,ROUND(NVL(PCK_NOMINA_COM10.FC_VR_INGRESO_PROM(HISTORICOS.COMPANIA,TO_DATE('01/07/' || HISTORICOS.ANO ,'DD/MM/YYYY'),TO_DATE('31/12/' || HISTORICOS.ANO ,'DD/MM/YYYY'),TERCERO.NIT),0),-3) AS PROM6
FROM COMPANIA INNER JOIN  TERCERO 
  ON COMPANIA.CODIGO = TERCERO.COMPANIA
LEFT JOIN TERCERO TER_COMPANIA
  ON COMPANIA.CODIGO                             = TER_COMPANIA.COMPANIA
 AND PCK_SYSMAN_UTL.FC_MINIT(COMPANIA.CODIGO,31) = TER_COMPANIA.NIT
 AND '001'                                       = TER_COMPANIA.SUCURSAL
INNER JOIN  PERSONAL 
  ON TERCERO.COMPANIA = PERSONAL.COMPANIA
 AND TERCERO.SUCURSAL = PERSONAL.SUCURSAL
 AND TERCERO.NIT      = PERSONAL.NUMERO_DCTO 
LEFT JOIN TIPOS_DOCUMENTOS 
  ON TERCERO.COMPANIA = TIPOS_DOCUMENTOS.COMPANIA
 AND TERCERO.TIPOID   = TIPOS_DOCUMENTOS.DCTO_IDENTIDAD
LEFT JOIN DEPARTAMENTO
  ON DEPARTAMENTO.PAIS   = COMPANIA.PAIS
 AND DEPARTAMENTO.CODIGO = COMPANIA.DEPARTAMENTO
LEFT JOIN CIUDAD 
  ON CIUDAD.PAIS         = COMPANIA.PAIS
 AND CIUDAD.DEPARTAMENTO = COMPANIA.DEPARTAMENTO
 AND CIUDAD.CODIGO       = COMPANIA.CIUDAD
INNER JOIN HISTORICOS 
  ON PERSONAL.COMPANIA = HISTORICOS.COMPANIA
 AND PERSONAL.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO 
INNER JOIN ANO
  ON HISTORICOS.COMPANIA = ANO.COMPANIA
 AND HISTORICOS.ANO      = ANO.NUMERO
INNER JOIN CONCEPTOS 
  ON HISTORICOS.COMPANIA       = CONCEPTOS.COMPANIA
 AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO    
INNER JOIN CONCEPTOS_ANO 
  ON HISTORICOS.COMPANIA       = CONCEPTOS_ANO.COMPANIA
 AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS_ANO.ID_DE_CONCEPTO    
 AND HISTORICOS.ANO            = CONCEPTOS_ANO.ANO    
INNER JOIN PERIODOS
  ON HISTORICOS.COMPANIA      = PERIODOS.COMPANIA
 AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO
 AND HISTORICOS.ANO           = PERIODOS.ANO
 AND HISTORICOS.MES           = PERIODOS.MES
 AND HISTORICOS.PERIODO       = PERIODOS.PERIODO
LEFT JOIN TIPOS_DOCUMENTOS TIPDOC2 
  ON PERSONAL.COMPANIA = TIPDOC2.COMPANIA
 AND PERSONAL.DCTO_IDENTIDAD_DEPEN = TIPDOC2.DCTO_IDENTIDAD
LEFT JOIN PARENTESCO 
  ON PERSONAL.COMPANIA = PARENTESCO.COMPANIA
 AND PERSONAL.PARENTESCO_DEPEN = PARENTESCO.PARENTESCO
WHERE CONCEPTOS_ANO.CODDIAN      IS NOT NULL
  AND (CONCEPTOS.CLASE IN (3,5) OR (CONCEPTOS.CLASE IN (8)))
  AND PERIODOS.ACUMULADO<>0
GROUP BY HISTORICOS.COMPANIA
     ,PCK_SYSMAN_UTL.FC_MINIT(HISTORICOS.COMPANIA,312)  
     ,PCK_SYSMAN_UTL.FC_MINIT(HISTORICOS.COMPANIA,31)   
     ,PCK_SYSMAN_UTL.FC_MINIT(HISTORICOS.COMPANIA,1) 
     ,TIPOS_DOCUMENTOS.CODIGODIAN 
     ,TERCERO.NIT 
     ,COMPANIA.NOMBRE 
     ,TER_COMPANIA.NOMBRE  
     ,TERCERO.APELLIDO1
     ,TERCERO.APELLIDO2
     ,TERCERO.NOMBRE1
     ,TERCERO.NOMBRE2
     ,(TRIM(TERCERO.NOMBRE1)  || ' ' ||TRIM(TERCERO.NOMBRE2)) 
     ,PERSONAL.REGIMEN
     ,TERCERO.DIRECCION
     ,HISTORICOS.ANO
     ,CIUDAD.CODIGODANE 
     ,CIUDAD.NOMBRE 
     ,DEPARTAMENTO.CODIGODANE 
     ,ANO.VALORUVT
     ,ANO.PATRIMONIOBRUTO_UVT
     ,ROUND(ANO.VALORUVT * ANO.PATRIMONIOBRUTO_UVT,-3) 
     ,ANO.INGRESOSTOTALES_UVT
     ,ROUND(ANO.VALORUVT * ANO.INGRESOSTOTALES_UVT,-3) 
     ,ANO.CONSUMOTARJETACRE_UVT
     ,ROUND(ANO.VALORUVT * ANO.CONSUMOTARJETACRE_UVT,-3) 
     ,ANO.COMPRASYCONSUMO_UVT
     ,ROUND(ANO.VALORUVT * ANO.COMPRASYCONSUMO_UVT,-3) 
     ,ANO.CONSIGNACIONESBANCARIAS_UVT
     ,ROUND(ANO.VALORUVT * ANO.CONSIGNACIONESBANCARIAS_UVT,-3)
     ,TIPDOC2.CODIGODIAN
	 ,PERSONAL.NUMERO_IDEN_DEPEN
	 ,PERSONAL.NOMBRES_DEPEN
	 ,PARENTESCO.DESCRIPCION;