CREATE OR REPLACE VIEW V_FONDO_DE_RIESGOS AS 
SELECT CF.COMPANIA, 
       CF.ID FONDO_RIESGOS, 
       F.NIT, 
       F.SUCURSAL,
       F.NOMBRE_FONDO NOMBRE_FONDO_RIESGOS,
       F.FORMA_PAGO, F.BANCO_PAGO, F.TIPO_CUENTA, F.CUENTA_CONSIGNAR, 
       F.DIRECCION DIRECCIONFR,
       F.TELEFONO TELEFONOFR,
       F.CORREOELECTRONICO CORREOELECTRONICOFR,
       F.CODA CODAIARP,
       F.PUBLICO
FROM CLASEFONDO CF 
INNER JOIN FONDO F ON CF.COMPANIA = F.COMPANIA  AND CF.ID_DE_FONDO = F.ID_DE_FONDO
WHERE CF.CLASE='ARL' WITH READ ONLY;