CREATE OR REPLACE VIEW V_FONDOS AS 
SELECT CF.COMPANIA, CF.ID COD_FONDO, CF.CLASE, F.NOMBRE_FONDO, F.NIT, F.ID_DE_FONDO, F.CODA, F.DOC_SIIF, F.COD_SIIF_PTO, 
F.RUBRO_SIIF_EMPLEADO, F.RUBRO_SIIF_PATRONO, F.PUBLICO, F.TIPO_CUENTA, F.CUENTA_CONSIGNAR, F.FORMA_PAGO, F.BANCO_PAGO
FROM CLASEFONDO CF 
INNER JOIN FONDO F ON CF.COMPANIA = F.COMPANIA  AND CF.ID_DE_FONDO = F.ID_DE_FONDO WITH READ ONLY ;