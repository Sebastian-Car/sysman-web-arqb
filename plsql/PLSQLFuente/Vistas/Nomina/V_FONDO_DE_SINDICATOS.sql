CREATE OR REPLACE VIEW V_FONDO_DE_SINDICATOS AS 
SELECT CF.COMPANIA, 
       CF.ID FONDO_SINDICATO, 
       F.NIT, 
       F.SUCURSAL,
       F.NOMBRE_FONDO NOMBRE_FONDO_SINDICATO,
       F.FORMA_PAGO, F.BANCO_PAGO, F.TIPO_CUENTA, F.CUENTA_CONSIGNAR, 
       F.DIRECCION DIRECCIONSF,
       F.TELEFONO TELEFONOSF,
       F.CORREOELECTRONICO CORREOELECTRONICOSF,
       F.CODA CODAISD,
       F.PUBLICO,
       F.DIASPRIMANAVIDAD,
       F.VALORCUOTAADICIONAL,
       F.CONCEPTOCUOTAADICIONAL       
FROM CLASEFONDO CF 
INNER JOIN FONDO F ON CF.COMPANIA = F.COMPANIA  AND CF.ID_DE_FONDO = F.ID_DE_FONDO
WHERE CF.CLASE='SIN' WITH READ ONLY ;