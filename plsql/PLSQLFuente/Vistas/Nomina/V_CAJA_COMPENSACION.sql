CREATE OR REPLACE VIEW V_CAJA_COMPENSACION AS 
SELECT CF.COMPANIA, 
       CF.ID CAJA_COMPENSACION, 
       F.NIT, 
       F.SUCURSAL,
       F.NOMBRE_FONDO NOMBRE_CAJA,
       F.FORMA_PAGO, F.BANCO_PAGO, F.TIPO_CUENTA, F.CUENTA_CONSIGNAR, 
       F.DIRECCION DIRECCIONCC,
       F.TELEFONO TELEFONOCC,
       F.CORREOELECTRONICO CORREOELECTRONICOCC,
       F.CODA CODAICC,
       F.PAIS,
       F.DEPARTAMENTO DEPARTAMENTOC,
       F.CIUDAD CIUDADC,
       F.PUBLICO, 
       F.DOC_SIIF  DOC_SIIF_CAJA,
       F.COD_SIIF_PTO COD_SIIF_CAJA,
       F.CAJAYSALUD
FROM CLASEFONDO CF 
INNER JOIN FONDO F ON CF.COMPANIA = F.COMPANIA  AND CF.ID_DE_FONDO = F.ID_DE_FONDO
WHERE CF.CLASE='CCF' WITH READ ONLY 
 ;