CREATE OR REPLACE VIEW V_FONDO_DE_SALUD AS 
SELECT CF.COMPANIA, 
       CF.ID FONDO_SALUD, 
       F.NIT, 
       F.SUCURSAL,
       F.NOMBRE_FONDO NOMBRE_FONDO_SALUD,
       F.FORMA_PAGO, F.BANCO_PAGO, F.TIPO_CUENTA, F.CUENTA_CONSIGNAR, 
       F.DIRECCION DIRECCIONFS,
       F.TELEFONO TELEFONOFS,
       F.CORREOELECTRONICO CORREOELECTRONICOFS,
       F.CODA CODAIFS,
       F.PUBLICO,
       F.RUBRO_SIIF_EMPLEADO,
       F.RUBRO_SIIF_PATRONO,
       F.DOC_SIIF DOC_SIIF_SALUD,
       F.COD_SIIF_PTO COD_SIIF_PTOS,
       F.CAJAYSALUD
FROM CLASEFONDO CF 
INNER JOIN FONDO F ON CF.COMPANIA = F.COMPANIA  AND CF.ID_DE_FONDO = F.ID_DE_FONDO
WHERE CF.CLASE='EPS' WITH READ ONLY ;