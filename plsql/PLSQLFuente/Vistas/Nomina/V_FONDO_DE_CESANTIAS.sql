CREATE OR REPLACE VIEW V_FONDO_DE_CESANTIAS AS 
SELECT CF.COMPANIA, 
       CF.ID FONDO_CESANTIAS, 
       F.NIT, 
       F.SUCURSAL,
       F.NOMBRE_FONDO NOMBRE_FONDO_CESANTIAS,
       F.FORMA_PAGO, F.BANCO_PAGO, F.TIPO_CUENTA, F.CUENTA_CONSIGNAR, 
       F.DIRECCION DIRECCIONFC,
       F.TELEFONO TELEFONOFC,
       F.CORREOELECTRONICO CORREOELECTRONICOFC,
       F.DOC_SIIF_CESANTIAS,
       F.CODA CODAIFC,
       F.PUBLICO
FROM CLASEFONDO CF 
INNER JOIN FONDO F ON CF.COMPANIA = F.COMPANIA  AND CF.ID_DE_FONDO = F.ID_DE_FONDO
WHERE CF.CLASE='CES' WITH READ ONLY ;