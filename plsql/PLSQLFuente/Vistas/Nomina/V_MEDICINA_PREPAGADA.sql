CREATE OR REPLACE VIEW V_MEDICINA_PREPAGADA AS 
SELECT CF.COMPANIA, 
       CF.ID MEDICINA_PREPAGADA, 
       F.NIT, 
       F.SUCURSAL,
       F.NOMBRE_FONDO NOMBRE_MEDICINA,
       F.FORMA_PAGO, F.BANCO_PAGO, F.TIPO_CUENTA, F.CUENTA_CONSIGNAR, 
       F.DIRECCION DIRECCIONMP,
       F.TELEFONO TELEFONOMP,
       F.CORREOELECTRONICO CORREOELECTRONICOMP,
       F.CODA CODAIMP,
       F.PUBLICO
FROM CLASEFONDO CF 
INNER JOIN FONDO F ON CF.COMPANIA = F.COMPANIA  AND CF.ID_DE_FONDO = F.ID_DE_FONDO
WHERE CF.CLASE='FMP' WITH READ ONLY;