CREATE OR REPLACE VIEW V_FONDO_DE_PENSION_AFC AS 
SELECT CF.COMPANIA, 
       CF.ID FONDO_AFC, 
       F.NIT, 
       F.NOMBRE_FONDO NOMBRE_FONDO_AFC,
       F.FORMA_PAGO, F.BANCO_PAGO, F.TIPO_CUENTA, F.CUENTA_CONSIGNAR, 
       F.DIRECCION DIRECCIONAFC,
       F.TELEFONO TELEFONOAFC,
       F.CORREOELECTRONICO CORREOELECTRONICOAFC,
       F.CODA CODAIAFC,
       F.COD_SIIF_PTO COD_SIIF_AFC,
       F.DOC_SIIF     DOC_SIIF_AFC,
       F.SUCURSAL,
       F.PUBLICO
FROM CLASEFONDO CF 
INNER JOIN FONDO F ON CF.COMPANIA = F.COMPANIA  AND CF.ID_DE_FONDO = F.ID_DE_FONDO
WHERE CF.CLASE='AFC'
    
WITH READ ONLY ;