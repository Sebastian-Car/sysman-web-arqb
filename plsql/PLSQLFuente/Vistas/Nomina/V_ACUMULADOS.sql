CREATE OR REPLACE VIEW V_ACUMULADOS AS 
SELECT 
  HISTORICOS.COMPANIA,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  HISTORICOS.ID_DE_EMPLEADO,
  HIS.SALARIO_BASE_IBC,
  HISTORICOS.ID_DE_CONCEPTO,
  CONCEPTOS.NOMBRE_CONCEPTO,
  CONCEPTOS.CLASE,
  CONCEPTOS.CODDIAN,
  DECODE(CONCEPTOS.CLASE,1,'Informativo',2,'Otros Informativos',
         3,'Remuneración',4,'Total Devengado',5,'Descuento',
         6,'Total Descuento',7,'Neto a Pagar',8,'Contable',
         9,'Anticipados',99,'Bases','Otros') CLASE_NOMBRE,  
  DECODE(CONCEPTOS.CLASE,1,'INFORMATIVOS',2,'OTROS INFORMATIVOS',
         3,'DEVENGOS',4,'VALOR TOTAL DEVENGOS',5,'DESCUENTOS',
         6,'VALOR TOTAL DESCUENTOS',7,'VALOR NETO A PAGAR',8,'CONCEPTOS CONTABLES',
         9,'ANTICIPADOS',99,'BASES','OTROS') CLASE_NOMBRE_MAYU,  
  HIS.NOMBRECOMPLETO,
  HIS.NOMBRES,
  HIS.APELLIDO1,
  HIS.APELLIDO2,
  HISTORICOS.FECHA,
  HIS.ID_CENTRO_DE_COSTO,
  HIS.NOMBRE_CENTRO_DE_COSTO,
  HIS.CUENTA,
  HISTORICOS.VALOR,
  CASE
    WHEN CONCEPTOS.CLASE IN(3)
    THEN HISTORICOS.VALOR
  ELSE
    0
  END DEVENGOS,
  CASE
    WHEN CONCEPTOS.CLASE IN(5)
    THEN HISTORICOS.VALOR
  ELSE
    0
  END DESCUENTOS,
  CASE 
  WHEN CONCEPTOS.CLASE IN(3)
  THEN HISTORICOS.VALOR
  ELSE
    CASE
    WHEN CONCEPTOS.CLASE IN(5)
    THEN HISTORICOS.VALOR*-1
    ELSE
    0
    END
  END  NETOAPAGAR,
  CASE
    WHEN CONCEPTOS.CLASE IN(7)
    THEN HISTORICOS.VALOR
  ELSE
    0
  END NETOHIS,
  CONCEPTOS.KARDEX,
  CONCEPTOS.TIPOAGRCONCEPTO,
  HIS.DEPENDENCIA,
  HIS.NOMBRE_DEPENDENCIA,
  HIS.BANCO,
  HIS.NOMBRE_BANCO,
  HIS.NOMBRE_DE_CARGO,
  HIS.ID_DE_CARGO,
  HIS.DCTO_IDENTIDAD,
  HIS.NUMERO_DCTO,
  HIS.FECHA_DE_INGRESO,
  HIS.FECHA_DE_RETIRO,
  HIS.FONDO_SALUD,
  HIS.NOMBRE_FONDOSALUD,
  HIS.ID_DEL_FONDO FONDO_PENSION,
  HIS.NOMBRE_FONDOPENSION,
  HIS.FONDO_RIESGOS,
  HIS.NOMBRE_FONDORIESGOS,
  HIS.FONDO_PENSION_VOL,
  HIS.NOMBRE_FONDOPENSION_VOL,
  HIS.NOMBRE_FONDOCESANTIAS,
  HIS.FONDO_CESANTIAS,
  HIS.CAJA_COMPENSACION,
  HIS.NOMBRE_CAJACOMPENSACION,
  HIS.NOMBRE_MEDICINAPREPAGADA,
  HIS.NOMBRE_ESTABLECIMIENTO,
  HIS.CODIGO_ESTABLECIMIENTO,
  HIS.ESCALAFON,
  HIS.NOMBRE_ESCALAFON,
  HIS.TIPOACTIVIDAD,
  HIS.NOMBRE_TIPOACTIVIDAD,
  HIS.ID_DE_CATEGORIA,
  HIS.NOMBRE_CATEGORIA,
  HIS.GRUPOCONTABLE,
  GC.NOMBRE GRUPOCONTABLE_NOM,
  PERIODOS.FECHAINICIO, 
  PERIODOS.FECHAFINAL,
  PERIODOS.NOM_PERIODO,  
   (
   SELECT HISTORICOSREL.VALOR
   FROM HISTORICOS HISTORICOSREL
   WHERE CONCEPTOS.COMPANIA        = HISTORICOSREL.COMPANIA
      AND CONCEPTOS.RELACIONADO    = HISTORICOSREL.ID_DE_CONCEPTO
      AND HISTORICOSREL.COMPANIA       = HISTORICOS.COMPANIA
      AND HISTORICOSREL.ID_DE_PROCESO  = HISTORICOS.ID_DE_PROCESO
      AND HISTORICOSREL.MES            = HISTORICOS.MES
      AND HISTORICOSREL.ANO            = HISTORICOS.ANO
      AND HISTORICOSREL.PERIODO        = HISTORICOS.PERIODO
      AND HISTORICOSREL.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
   ) CANTIDAD
FROM HISTORICOS
LEFT JOIN PERSONAL_HISTORICO HIS
      ON HIS.COMPANIA        = HISTORICOS.COMPANIA
      AND HIS.ID_DE_PROCESO  = HISTORICOS.ID_DE_PROCESO
      AND HIS.MES            = HISTORICOS.MES
      AND HIS.ANO            = HISTORICOS.ANO
      AND HIS.PERIODO        = HISTORICOS.PERIODO
      AND HIS.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
INNER JOIN CONCEPTOS 
     ON CONCEPTOS.COMPANIA        = HISTORICOS.COMPANIA
     AND CONCEPTOS.ID_DE_CONCEPTO = HISTORICOS.ID_DE_CONCEPTO
INNER JOIN PERIODOS
        ON HISTORICOS.COMPANIA        = PERIODOS.COMPANIA
        AND HISTORICOS.ID_DE_PROCESO  = PERIODOS.ID_DE_PROCESO
        AND HISTORICOS.MES            = PERIODOS.MES
        AND HISTORICOS.ANO            = PERIODOS.ANO
        AND HISTORICOS.PERIODO        = PERIODOS.PERIODO
LEFT JOIN GRUPO_CONTABLE GC
      ON GC.CODIGO        = HIS.GRUPOCONTABLE        
WHERE PERIODOS.ACUMULADO<>0 WITH READ ONLY
 
;