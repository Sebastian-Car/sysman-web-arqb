CREATE OR REPLACE VIEW V_RES_CESANTIASPARCIALES AS 
SELECT 
  PERSONAL_HISTORICO.COMPANIA,
  PERSONAL_HISTORICO.ID_DE_PROCESO,
  PERSONAL_HISTORICO.ANO,
  PERSONAL_HISTORICO.MES,
  PERSONAL_HISTORICO.PERIODO,
  PERSONAL_HISTORICO.ID_DE_EMPLEADO,
  PERSONAL_HISTORICO.NOMBRECOMPLETO,
  PERSONAL_HISTORICO.NUMERO_DCTO, 
  PERSONAL_HISTORICO.NOMBRE_EXPEDIDA_CED CIUDADCEDULA,
  PERSONAL_HISTORICO.FECHA_DE_INGRESO,   
  PERSONAL_HISTORICO.NOMBRE_DE_CARGO,
  PERSONAL_HISTORICO.NOMBRE_DEPENDENCIA, 
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=177 THEN HISTORICOS.VALOR ELSE 0 END)  CESANTIAS, 
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=169 THEN HISTORICOS.VALOR ELSE 0 END) INTERECES_CESANTIASD, 
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=1 THEN HISTORICOS.VALOR ELSE 0 END)  SALARIO_BASICO_MENSUAL, 
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=177 THEN HISTORICOS.VALOR ELSE 0 END)
   +SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=169 THEN HISTORICOS.VALOR ELSE 0 END)  TOTALPAGO 
FROM PERSONAL_HISTORICO
INNER JOIN HISTORICOS
    ON PERSONAL_HISTORICO.COMPANIA = HISTORICOS.COMPANIA 
   AND PERSONAL_HISTORICO.ID_DE_PROCESO = HISTORICOS.ID_DE_PROCESO
   AND PERSONAL_HISTORICO.ANO = HISTORICOS.ANO
   AND PERSONAL_HISTORICO.MES = HISTORICOS.MES
   AND PERSONAL_HISTORICO.PERIODO = HISTORICOS.PERIODO
   AND PERSONAL_HISTORICO.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
GROUP BY 
  PERSONAL_HISTORICO.COMPANIA,
  PERSONAL_HISTORICO.ID_DE_PROCESO,
  PERSONAL_HISTORICO.ANO,
  PERSONAL_HISTORICO.MES,
  PERSONAL_HISTORICO.PERIODO,
  PERSONAL_HISTORICO.ID_DE_EMPLEADO,
  PERSONAL_HISTORICO.NOMBRECOMPLETO,
  PERSONAL_HISTORICO.NUMERO_DCTO, 
  PERSONAL_HISTORICO.NOMBRE_EXPEDIDA_CED,
  PERSONAL_HISTORICO.FECHA_DE_INGRESO,   
  PERSONAL_HISTORICO.NOMBRE_DE_CARGO,
  PERSONAL_HISTORICO.NOMBRE_DEPENDENCIA
HAVING SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=177 THEN HISTORICOS.VALOR ELSE 0 END)
   +SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=169 THEN HISTORICOS.VALOR ELSE 0 END)   > 0 WITH READ ONLY;