CREATE OR REPLACE VIEW V_FONDO_DE_PENSIONES AS 
SELECT CF.COMPANIA, 
       CF.ID ID_DEL_FONDO, 
       F.NIT,
       F.SUCURSAL,
       F.NOMBRE_FONDO NOMBRE_DEL_FONDO,
       F.FORMA_PAGO, F.BANCO_PAGO, F.TIPO_CUENTA, F.CUENTA_CONSIGNAR, 
       F.DIRECCION DIRECCIONFP,
       F.TELEFONO TELEFONOFP,
       F.CORREOELECTRONICO CORREOELECTRONICOFP,
       F.CODA CODAIFP,
       F.PUBLICO,
       F.DOC_SIIF DOC_SIIF_PENSION,
       F.COD_SIIF_PTO COD_SIIF_PTOP
FROM CLASEFONDO CF 
INNER JOIN FONDO F ON CF.COMPANIA = F.COMPANIA  AND CF.ID_DE_FONDO = F.ID_DE_FONDO
WHERE CF.CLASE='AFP' WITH READ ONLY 
;