CREATE OR REPLACE VIEW V_NOVEDADES AS 
SELECT 
  HIS.COMPANIA,
  HIS.ID_DE_PROCESO,
  HIS.ANO,
  HIS.MES,
  HIS.PERIODO,
  HIS.ID_DE_EMPLEADO,
  HIS.NOMBRECOMPLETO,
  HIS.ID_CENTRO_DE_COSTO,
  HIS.NOMBRE_CENTRO_DE_COSTO,
  HIS.CUENTA,
  HIS.SALARIO_BASE_IBC,
  HIS.DEPENDENCIA,
  HIS.NOMBRE_DEPENDENCIA,
  HIS.BANCO,
  HIS.NOMBRE_BANCO,
  HIS.NOMBRE_DE_CARGO,
  HIS.ID_DE_CARGO,
  HIS.NUMERO_DCTO,
  HIS.FECHA_DE_INGRESO,
  HIS.NOMBRE_FONDOSALUD,
  HIS.NOMBRE_FONDOPENSION,
  HIS.NOMBRE_FONDORIESGOS,
  HIS.NOMBRE_FONDOPENSION_VOL,
  HIS.NOMBRE_FONDOCESANTIAS,
  HIS.NOMBRE_CAJACOMPENSACION,
  HIS.NOMBRE_MEDICINAPREPAGADA,
  
  NOVEDADES.ID_DE_CONCEPTO,
  CONCEPTOS.NOMBRE_CONCEPTO,
  CONCEPTOS.CLASE,
  DECODE(CONCEPTOS.CLASE,1,'Informativo',2,'Otros Informativos',
         3,'Remuneración',4,'Total Devengado',5,'Descuento',
         6,'Total Descuento',7,'Neto a Pagar',8,'Contable',
         9,'Anticipados',99,'Bases','Otros') CLASE_NOMBRE,  
  CASE
    WHEN CONCEPTOS.CLASE IN(3)
    THEN NOVEDADES.VALOR
  ELSE
    0
  END DEVENGOS,
  CASE
    WHEN CONCEPTOS.CLASE IN(5)
    THEN NOVEDADES.VALOR
  ELSE
    0
  END DESCUENTOS,
  CASE 
  WHEN CONCEPTOS.CLASE IN(3)
  THEN NOVEDADES.VALOR
  ELSE
    CASE
    WHEN CONCEPTOS.CLASE IN(5)
    THEN NOVEDADES.VALOR*-1
    ELSE
    0
    END
  END  NETOAPAGAR,
  CONCEPTOS.KARDEX,
  CONCEPTOS.TIPOAGRCONCEPTO,
  NOVEDADES.FECHA,  
  NOVEDADES.VALOR,
  PERIODOS.FECHAINICIO, 
  PERIODOS.FECHAFINAL,
  PERIODOS.NOM_PERIODO
FROM PERSONAL_HISTORICO HIS
INNER JOIN NOVEDADES
      ON HIS.COMPANIA        = NOVEDADES.COMPANIA
      AND HIS.ID_DE_PROCESO  = NOVEDADES.ID_DE_PROCESO
      AND HIS.MES            = NOVEDADES.MES
      AND HIS.ANO            = NOVEDADES.ANO
      AND HIS.PERIODO        = NOVEDADES.PERIODO
      AND HIS.ID_DE_EMPLEADO = NOVEDADES.ID_DE_EMPLEADO
INNER JOIN CONCEPTOS 
     ON CONCEPTOS.COMPANIA        = NOVEDADES.COMPANIA
     AND CONCEPTOS.ID_DE_CONCEPTO = NOVEDADES.ID_DE_CONCEPTO
INNER JOIN PERIODOS
        ON NOVEDADES.COMPANIA        = PERIODOS.COMPANIA
        AND NOVEDADES.ID_DE_PROCESO  = PERIODOS.ID_DE_PROCESO
        AND NOVEDADES.MES            = PERIODOS.MES
        AND NOVEDADES.ANO            = PERIODOS.ANO
        AND NOVEDADES.PERIODO        = PERIODOS.PERIODO
WITH READ ONLY;