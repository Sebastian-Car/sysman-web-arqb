CREATE OR REPLACE VIEW V_SP_RECAUDOS AS 
WITH CONVENIOS AS (SELECT CON.COMPANIA, 
                          CON.FECHA_PAGO, 
                          CON.BANCO_PAGO, 
                          CON.PAQUETE_PAGO, 
                          SUM(CON.TOTAL)  CONVENIOS
                FROM SP_HISTORIA_CONVENIOS CON
                GROUP BY  CON.COMPANIA, 
                          CON.FECHA_PAGO, 
                          CON.BANCO_PAGO, 
                          CON.PAQUETE_PAGO
                ),
    TERCERIZADO AS (SELECT TER.COMPANIA, 
                           TER.FECHA_PAGO, 
                           TER.BANCO_PAGO, 
                           TER.PAQUETE_PAGO, 
                           SUM(TER.VALORASEO) AS TERCERIZADO
                  FROM SP_HISTORIA_EXTERNA TER
                  GROUP BY TER.COMPANIA, 
                           TER.FECHA_PAGO, 
                           TER.BANCO_PAGO, 
                           TER.PAQUETE_PAGO
  )
SELECT SP_RECAUDOS.COMPANIA, 
       SP_RECAUDOS.BANCO, 
       SP_RECAUDOS.NUMEROPAQUETE,
       TO_DATE(TO_CHAR(SP_RECAUDOS.FECHA,'DD/MM/YYYY'),'DD/MM/YYYY') FECHA, 
       TO_NUMBER(TO_CHAR(SP_RECAUDOS.FECHA,'YYYY')) ANO, 
       TO_NUMBER(TO_CHAR(SP_RECAUDOS.FECHA,'MM')) MES, 
       TO_NUMBER(TO_CHAR(SP_RECAUDOS.FECHA,'DD')) DIA, 
       PCK_SYSMAN_UTL.FC_NOMBRE_MES(TO_NUMBER(TO_CHAR(SP_RECAUDOS.FECHA,'MM'))) MESNOMBRE,
       SP_BANCOS.NOMBRE BANCO_NOM,
       SP_RECAUDOS.CUPONESREPORTADOS, 
       SP_RECAUDOS.CUPONESREGISTRADOS, 
       SP_RECAUDOS.VALORREPORTADO, 
       SP_RECAUDOS.VALORREGISTRADO,
       SP_RECAUDOS.DIFERENCIACUPONES,
       SP_RECAUDOS.DIFERENCIAVALORES,
       SP_RECAUDOS.COMENTARIOS,
       NVL(CONVENIOS.CONVENIOS,0) CONVENIOS, 
       NVL(TERCERIZADO.TERCERIZADO,0) TERCERIZADO,
       SP_RECAUDOS.VALORREGISTRADO + NVL(CONVENIOS.CONVENIOS,0) + NVL(TERCERIZADO.TERCERIZADO,0) TOTAL       
FROM SP_RECAUDOS INNER JOIN SP_BANCOS
  ON SP_RECAUDOS.COMPANIA = SP_BANCOS.COMPANIA 
 AND SP_RECAUDOS.BANCO    = SP_BANCOS.CODIGO 
 LEFT JOIN CONVENIOS
  ON SP_RECAUDOS.COMPANIA      = CONVENIOS.COMPANIA
 AND SP_RECAUDOS.BANCO         = CONVENIOS.BANCO_PAGO 
 AND SP_RECAUDOS.NUMEROPAQUETE = CONVENIOS.PAQUETE_PAGO  
 AND SP_RECAUDOS.FECHA         = CONVENIOS.FECHA_PAGO
 LEFT JOIN TERCERIZADO
  ON SP_RECAUDOS.COMPANIA      = TERCERIZADO.COMPANIA
 AND SP_RECAUDOS.BANCO         = TERCERIZADO.BANCO_PAGO 
 AND SP_RECAUDOS.NUMEROPAQUETE = TERCERIZADO.PAQUETE_PAGO  
 AND SP_RECAUDOS.FECHA         = TERCERIZADO.FECHA_PAGO 
 ;