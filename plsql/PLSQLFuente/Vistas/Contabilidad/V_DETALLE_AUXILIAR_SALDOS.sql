CREATE OR REPLACE VIEW V_DETALLE_AUXILIAR_SALDOS AS 
SELECT DETALLE_COMPROBANTE_CNT.COMPANIA,
       DETALLE_COMPROBANTE_CNT.ANO, 
       DETALLE_COMPROBANTE_CNT.TIPO_CPTE,
       TIPO_COMPROBANTE.NOMBRE TIPO_COMPROBANTE_NOM,
       DETALLE_COMPROBANTE_CNT.COMPROBANTE, 
       DETALLE_COMPROBANTE_CNT.CONSECUTIVO,
       DETALLE_COMPROBANTE_CNT.FECHA,
       DETALLE_COMPROBANTE_CNT.MES, 
       V_PLAN_CONTABLE.CODIGO CODIGO_CUENTA, 
       V_PLAN_CONTABLE.NOMBRE CUENTANOMBRE,
       DETALLE_COMPROBANTE_CNT.DESCRIPCION  DESCRIPCION_DET,
       COMPROBANTE_CNT.DESCRIPCION DESCRIPCION, 
       TIPO_COMPROBANTE.CLASE_CONTABLE,
       DETALLE_COMPROBANTE_CNT.NRO_DOCUMENTO NRO_DOCUMENTO_DET,
       COMPROBANTE_CNT.NRO_DOCUMENTO,
       
       V_PLAN_CONTABLE.CENTRO_COSTO CENTRO_COSTO,
       CENTRO_COSTO.NOMBRE CENTRO_COSTO_NOM,       
       DETALLE_COMPROBANTE_CNT.CENTRO_COSTO CENTRO_COSTO_DET,
       CENTRO_COSTO_DET.NOMBRE CENTRO_COSTO_DET_NOM,
       CASE WHEN MAN_CEN_CTO IN(0) THEN '' ELSE DETALLE_COMPROBANTE_CNT.CENTRO_COSTO END CENTRO_COSTO_VIS,
       
       V_PLAN_CONTABLE.TERCERO  TERCERO,
       V_PLAN_CONTABLE.SUCURSAL SUCURSAL,
       TERCERO.NOMBRE TERCERO_NOM,
              
       DETALLE_COMPROBANTE_CNT.TERCERO TERCERO_DET,
       DETALLE_COMPROBANTE_CNT.SUCURSAL SUCURSAL_DET,
       TERCERO_DET.NOMBRE TERCERO_DET_NOM,
       CASE WHEN MAN_AUX_TER IN(0) THEN '' ELSE DETALLE_COMPROBANTE_CNT.TERCERO  END TERCERO_VIS,
       CASE WHEN MAN_AUX_TER IN(0) THEN '' ELSE DETALLE_COMPROBANTE_CNT.SUCURSAL END SUCURSAL_VIS,
       
       V_PLAN_CONTABLE.AUXILIAR AUXILIAR,
       AUXILIAR.NOMBRE AUXILIAR_NOM,       
       DETALLE_COMPROBANTE_CNT.AUXILIAR AUXILIAR_DET,
       AUXILIAR_DET.NOMBRE AUXILIAR_DET_NOM,
       CASE WHEN MAN_AUX_GEN IN(0) THEN '' ELSE DETALLE_COMPROBANTE_CNT.AUXILIAR END  AUXILIAR_VIS,
       
       V_PLAN_CONTABLE.REFERENCIA REFERENCIA,
       REFERENCIA.NOMBRE REFERENCIA_NOM,       
       DETALLE_COMPROBANTE_CNT.REFERENCIA REFERENCIA_DET,
       REFERENCIA_DET.NOMBRE REFERENCIA_DET_NOM,
       CASE WHEN MAN_AUX_REF IN(0) THEN '' ELSE DETALLE_COMPROBANTE_CNT.REFERENCIA END  REFERENCIA_VIS,
       
       V_PLAN_CONTABLE.FUENTE_RECURSO FUENTE_RECURSO,
       FUENTE_RECURSOS.NOMBRE FUENTE_RECURSO_NOM,       
       DETALLE_COMPROBANTE_CNT.FUENTE_RECURSO FUENTE_RECURSO_DET,
       FUENTE_RECURSOS_DET.NOMBRE FUENTE_RECURSO_DET_NOM,
       CASE WHEN MAN_AUX_FUE IN(0) THEN '' ELSE DETALLE_COMPROBANTE_CNT.FUENTE_RECURSO END FUENTE_RECURSOS_VIS, 
       
       DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE,
       CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =1 THEN V_PLAN_CONTABLE.SALDO0 ELSE
        CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =2 THEN V_PLAN_CONTABLE.SALDO1 ELSE
          CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =3 THEN V_PLAN_CONTABLE.SALDO2 ELSE
            CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =4 THEN V_PLAN_CONTABLE.SALDO3 ELSE
              CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =5 THEN V_PLAN_CONTABLE.SALDO4 ELSE
                CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =6 THEN V_PLAN_CONTABLE.SALDO5 ELSE
                  CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =7 THEN V_PLAN_CONTABLE.SALDO6 ELSE
                    CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =8 THEN V_PLAN_CONTABLE.SALDO7 ELSE
                      CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =9 THEN V_PLAN_CONTABLE.SALDO8 ELSE
                        CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =10 THEN V_PLAN_CONTABLE.SALDO9 ELSE
                          CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =11 THEN V_PLAN_CONTABLE.SALDO10 ELSE
                            CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =12 THEN V_PLAN_CONTABLE.SALDO11 ELSE
                              CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =13 THEN V_PLAN_CONTABLE.SALDO12 ELSE
                              0
                              END
                            END
                          END
                        END
                      END
                    END
                  END
                END
              END
            END
          END
        END
       END + NVL(FRACCION.SALDOALDIA,0) SALDO_ANT,
       DETALLE_COMPROBANTE_CNT.VALOR_DEBITO, 
       DETALLE_COMPROBANTE_CNT.VALOR_CREDITO
FROM V_PLAN_CONTABLE INNER JOIN DETALLE_COMPROBANTE_CNT 
 ON  DETALLE_COMPROBANTE_CNT.COMPANIA = V_PLAN_CONTABLE.COMPANIA 
 AND DETALLE_COMPROBANTE_CNT.ANO      = V_PLAN_CONTABLE.ANO 
 AND DETALLE_COMPROBANTE_CNT.CUENTA   = V_PLAN_CONTABLE.CODIGO
 AND V_PLAN_CONTABLE.CENTRO_COSTO    = CASE WHEN MAN_CEN_CTO IN(0) THEN '99999999999999999999' ELSE DETALLE_COMPROBANTE_CNT.CENTRO_COSTO   END
 AND V_PLAN_CONTABLE.TERCERO         = CASE WHEN MAN_AUX_TER IN(0) THEN '999999999999999999'   ELSE DETALLE_COMPROBANTE_CNT.TERCERO        END
 AND V_PLAN_CONTABLE.SUCURSAL        = CASE WHEN MAN_AUX_TER IN(0) THEN '999'                  ELSE DETALLE_COMPROBANTE_CNT.SUCURSAL       END
 AND V_PLAN_CONTABLE.AUXILIAR        = CASE WHEN MAN_AUX_GEN IN(0) THEN '99999999999999999999' ELSE DETALLE_COMPROBANTE_CNT.AUXILIAR       END
 AND V_PLAN_CONTABLE.REFERENCIA      = CASE WHEN MAN_AUX_REF IN(0) THEN '99999999999999999999' ELSE DETALLE_COMPROBANTE_CNT.REFERENCIA     END
 AND V_PLAN_CONTABLE.FUENTE_RECURSO  = CASE WHEN MAN_AUX_FUE IN(0) THEN '99999999999999999999' ELSE DETALLE_COMPROBANTE_CNT.FUENTE_RECURSO END
 
 INNER JOIN COMPROBANTE_CNT 
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA    = COMPROBANTE_CNT.COMPANIA 
    AND DETALLE_COMPROBANTE_CNT.ANO         = COMPROBANTE_CNT.ANO 
    AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE   = COMPROBANTE_CNT.TIPO 
    AND DETALLE_COMPROBANTE_CNT.COMPROBANTE = COMPROBANTE_CNT.NUMERO
  INNER JOIN TIPO_COMPROBANTE 
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA  = TIPO_COMPROBANTE.COMPANIA 
    AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO 
  INNER JOIN CENTRO_COSTO 
   ON  V_PLAN_CONTABLE.COMPANIA      = CENTRO_COSTO.COMPANIA 
   AND V_PLAN_CONTABLE.ANO           = CENTRO_COSTO.ANO 
   AND V_PLAN_CONTABLE.CENTRO_COSTO  = CENTRO_COSTO.CODIGO
  INNER JOIN CENTRO_COSTO CENTRO_COSTO_DET 
   ON  DETALLE_COMPROBANTE_CNT.COMPANIA     = CENTRO_COSTO_DET.COMPANIA 
   AND DETALLE_COMPROBANTE_CNT.ANO          = CENTRO_COSTO_DET.ANO 
   AND DETALLE_COMPROBANTE_CNT.CENTRO_COSTO = CENTRO_COSTO_DET.CODIGO 
   
  INNER JOIN TERCERO 
   ON  V_PLAN_CONTABLE.COMPANIA   = TERCERO.COMPANIA 
   AND V_PLAN_CONTABLE.TERCERO    = TERCERO.NIT 
   AND V_PLAN_CONTABLE.SUCURSAL   = TERCERO.SUCURSAL    
   
  INNER JOIN TERCERO TERCERO_DET
   ON  DETALLE_COMPROBANTE_CNT.COMPANIA = TERCERO_DET.COMPANIA 
   AND DETALLE_COMPROBANTE_CNT.TERCERO  = TERCERO_DET.NIT 
   AND DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO_DET.SUCURSAL   
   
  INNER JOIN AUXILIAR 
    ON  V_PLAN_CONTABLE.COMPANIA   = AUXILIAR.COMPANIA 
  	AND V_PLAN_CONTABLE.ANO        = AUXILIAR.ANO 
    AND V_PLAN_CONTABLE.AUXILIAR   = AUXILIAR.CODIGO    
  INNER JOIN AUXILIAR AUXILIAR_DET
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA = AUXILIAR_DET.COMPANIA 
  	AND DETALLE_COMPROBANTE_CNT.ANO      = AUXILIAR_DET.ANO 
    AND DETALLE_COMPROBANTE_CNT.AUXILIAR = AUXILIAR_DET.CODIGO    

  INNER JOIN REFERENCIA 
    ON  V_PLAN_CONTABLE.COMPANIA    = REFERENCIA.COMPANIA 
  	AND V_PLAN_CONTABLE.ANO         = REFERENCIA.ANO 
    AND V_PLAN_CONTABLE.REFERENCIA  = REFERENCIA.CODIGO    
  INNER JOIN REFERENCIA REFERENCIA_DET
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA   = REFERENCIA_DET.COMPANIA 
  	AND DETALLE_COMPROBANTE_CNT.ANO        = REFERENCIA_DET.ANO 
    AND DETALLE_COMPROBANTE_CNT.REFERENCIA = REFERENCIA_DET.CODIGO

  INNER JOIN FUENTE_RECURSOS 
    ON  V_PLAN_CONTABLE.COMPANIA         = FUENTE_RECURSOS.COMPANIA 
  	AND V_PLAN_CONTABLE.ANO              = FUENTE_RECURSOS.ANO 
    AND V_PLAN_CONTABLE.FUENTE_RECURSO   = FUENTE_RECURSOS.CODIGO    
    
  INNER JOIN FUENTE_RECURSOS FUENTE_RECURSOS_DET
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA       = FUENTE_RECURSOS_DET.COMPANIA 
  	AND DETALLE_COMPROBANTE_CNT.ANO            = FUENTE_RECURSOS_DET.ANO 
    AND DETALLE_COMPROBANTE_CNT.FUENTE_RECURSO = FUENTE_RECURSOS_DET.CODIGO
    
  LEFT JOIN (--SE GENERA EL SALDO TENIENDO EN CUENTA LOS INDICADORES DE LA CUENTA
             SELECT PLAN.COMPANIA, PLAN.ANO, PLAN.CODIGO,
                   CASE WHEN MAN_CEN_CTO IN(0) THEN '99999999999999999999' ELSE DETFRACC.CENTRO_COSTO   END CENTRO_COSTO, 
                   CASE WHEN MAN_AUX_TER IN(0) THEN '999999999999999999'   ELSE DETFRACC.TERCERO        END TERCERO,
                   CASE WHEN MAN_AUX_TER IN(0) THEN '999'                  ELSE DETFRACC.SUCURSAL       END SUCURSAL,
                   CASE WHEN MAN_AUX_GEN IN(0) THEN '99999999999999999999' ELSE DETFRACC.AUXILIAR       END AUXILIAR,
                   CASE WHEN MAN_AUX_REF IN(0) THEN '99999999999999999999' ELSE DETFRACC.REFERENCIA     END REFERENCIA,
                   CASE WHEN MAN_AUX_FUE IN(0) THEN '99999999999999999999' ELSE DETFRACC.FUENTE_RECURSO END FUENTE_RECURSO,
                   SUM( CASE WHEN PLAN.NATURALEZA='D' 
                      THEN DETFRACC.VALOR_DEBITO  -DETFRACC.VALOR_CREDITO
                      ELSE DETFRACC.VALOR_CREDITO -DETFRACC.VALOR_DEBITO
                      END
                      ) SALDOALDIA
             FROM DETALLE_COMPROBANTE_CNT DETFRACC INNER JOIN V_PLAN_CONTABLE PLAN
              ON  DETFRACC.COMPANIA = PLAN.COMPANIA 
              AND DETFRACC.ANO      = PLAN.ANO 
              AND DETFRACC.CUENTA   = PLAN.CODIGO
             WHERE PLAN.COMPANIA= PCK_ENTORNO.FC_GETCOMPANIA
               AND PLAN.ANO     = PCK_ENTORNO.FC_GETANIOINICIAL
               AND PLAN.CODIGO  BETWEEN PCK_ENTORNO.FC_GETCODIGOINICIAL AND PCK_ENTORNO.FC_GETCODIGOFINAL 
               AND DETFRACC.TIPO_CPTE BETWEEN 'AAA' AND 'ZZZZ' 
               AND DETFRACC.FECHA <  PCK_ENTORNO.FC_GETFECHAINICIAL
               AND DETFRACC.FECHA >= TO_DATE('01/' || TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM') || TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'YYYY'))
             GROUP BY  PLAN.COMPANIA, PLAN.ANO, PLAN.CODIGO,
                   CASE WHEN MAN_CEN_CTO IN(0) THEN '99999999999999999999' ELSE DETFRACC.CENTRO_COSTO   END , 
                   CASE WHEN MAN_AUX_TER IN(0) THEN '999999999999999999'   ELSE DETFRACC.TERCERO        END ,
                   CASE WHEN MAN_AUX_TER IN(0) THEN '999'                  ELSE DETFRACC.SUCURSAL       END ,
                   CASE WHEN MAN_AUX_GEN IN(0) THEN '99999999999999999999' ELSE DETFRACC.AUXILIAR       END ,
                   CASE WHEN MAN_AUX_REF IN(0) THEN '99999999999999999999' ELSE DETFRACC.REFERENCIA     END ,
                   CASE WHEN MAN_AUX_FUE IN(0) THEN '99999999999999999999' ELSE DETFRACC.FUENTE_RECURSO END 
              ) FRACCION
    ON FRACCION.COMPANIA       = V_PLAN_CONTABLE.COMPANIA           
   AND FRACCION.ANO            = V_PLAN_CONTABLE.ANO            
   AND FRACCION.CODIGO         = V_PLAN_CONTABLE.CODIGO
   AND FRACCION.CENTRO_COSTO   = V_PLAN_CONTABLE.CENTRO_COSTO   
   AND FRACCION.TERCERO        = V_PLAN_CONTABLE.TERCERO
   AND FRACCION.SUCURSAL       = V_PLAN_CONTABLE.SUCURSAL
   AND FRACCION.AUXILIAR       = V_PLAN_CONTABLE.AUXILIAR
   AND FRACCION.REFERENCIA     = V_PLAN_CONTABLE.REFERENCIA
   AND FRACCION.FUENTE_RECURSO = V_PLAN_CONTABLE.FUENTE_RECURSO
   
WHERE V_PLAN_CONTABLE.COMPANIA= PCK_ENTORNO.FC_GETCOMPANIA
  AND V_PLAN_CONTABLE.ANO     = PCK_ENTORNO.FC_GETANIOINICIAL
  AND V_PLAN_CONTABLE.CODIGO  BETWEEN PCK_ENTORNO.FC_GETCODIGOINICIAL AND PCK_ENTORNO.FC_GETCODIGOFINAL 
  AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE BETWEEN 'AAA' AND 'ZZZZ'
  AND DETALLE_COMPROBANTE_CNT.FECHA BETWEEN PCK_ENTORNO.FC_GETFECHAINICIAL AND PCK_ENTORNO.FC_GETFECHAFINAL
  
UNION ALL

 SELECT SIN_DET."COMPANIA",SIN_DET."ANO",SIN_DET."TIPO_CPTE",SIN_DET."TIPO_COMPROBANTE_NOM",SIN_DET."COMPROBANTE",SIN_DET."CONSECUTIVO",SIN_DET."FECHA",SIN_DET."MES",SIN_DET."CODIGO_CUENTA",SIN_DET."CUENTANOMBRE",SIN_DET."DESCRIPCION_DET",SIN_DET."DESCRIPCION",SIN_DET."CLASE_CONTABLE",SIN_DET."NRO_DOCUMENTO_DET",SIN_DET."NRO_DOCUMENTO",SIN_DET."CENTRO_COSTO",SIN_DET."CENTRO_COSTO_NOM",SIN_DET."CENTRO_COSTO_DET",SIN_DET."CENTRO_COSTO_DET_NOM",SIN_DET."CENTRO_COSTO_VIS",SIN_DET."TERCERO",SIN_DET."SUCURSAL",SIN_DET."TERCERO_NOM",SIN_DET."TERCERO_DET",SIN_DET."SUCURSAL_DET",SIN_DET."TERCERO_DET_NOM",SIN_DET."TERCERO_VIS",SIN_DET."SUCURSAL_VIS",SIN_DET."AUXILIAR",SIN_DET."AUXILIAR_NOM",SIN_DET."AUXILIAR_DET",SIN_DET."AUXILIAR_DET_NOM",SIN_DET."AUXILIAR_VIS",SIN_DET."REFERENCIA",SIN_DET."REFERENCIA_NOM",SIN_DET."REFERENCIA_DET",SIN_DET."REFERENCIA_DET_NOM",SIN_DET."REFERENCIA_VIS",SIN_DET."FUENTE_RECURSO",SIN_DET."FUENTE_RECURSO_NOM",SIN_DET."FUENTE_RECURSO_DET",SIN_DET."FUENTE_RECURSO_DET_NOM",SIN_DET."FUENTE_RECURSOS_VIS",SIN_DET."BASE_GRAVABLE",SIN_DET."SALDO_ANT",SIN_DET."VALOR_DEBITO",SIN_DET."VALOR_CREDITO"
 FROM (
       SELECT PLAN_CONTABLE.COMPANIA, 
              PLAN_CONTABLE.ANO, 
              '' TIPO_CPTE,
              '' TIPO_COMPROBANTE_NOM,
              NULL COMPROBANTE,
              NULL CONSECUTIVO,
              NULL FECHA,
              NULL MES,
              PLAN_CONTABLE.CODIGO CODIGO_CUENTA,
              PLAN_CONTABLE.NOMBRE CUENTANOMBRE,              
              '' DESCRIPCION_DET,
              '' DESCRIPCION,
              '' CLASE_CONTABLE,
              '' NRO_DOCUMENTO_DET,
              '' NRO_DOCUMENTO,
              SALDO.CENTRO_COSTO,
              CENTRO_COSTO.NOMBRE CENTRO_COSTO_NOM,
              '' CENTRO_COSTO_DET,
              '' CENTRO_COSTO_DET_NOM, 
              '' CENTRO_COSTO_VIS,        
              SALDO.TERCERO,
              SALDO.SUCURSAL,
              TERCERO.NOMBRE TERCERO_NOM,
              '' TERCERO_DET,
              '' SUCURSAL_DET,
              '' TERCERO_DET_NOM,
              '' TERCERO_VIS,
              '' SUCURSAL_VIS,
              SALDO.AUXILIAR,
              AUXILIAR.NOMBRE AUXILIAR_NOM,       
              '' AUXILIAR_DET,
              '' AUXILIAR_DET_NOM,
              '' AUXILIAR_VIS,
              SALDO.REFERENCIA,
              REFERENCIA.NOMBRE REFERENCIA_NOM,       
              '' REFERENCIA_DET,
              '' REFERENCIA_DET_NOM,
              '' REFERENCIA_VIS,
              
              SALDO.FUENTE_RECURSO,
              FUENTE_RECURSOS.NOMBRE FUENTE_RECURSO_NOM,       
              '' FUENTE_RECURSO_DET,
              '' FUENTE_RECURSO_DET_NOM,
              '' FUENTE_RECURSOS_VIS, 
              
              0 BASE_GRAVABLE,
              
              CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =1 THEN SALDO.SALDO0 ELSE
                CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =2 THEN SALDO.SALDO1 ELSE
                  CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =3 THEN SALDO.SALDO2 ELSE
                    CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =4 THEN SALDO.SALDO3 ELSE
                      CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =5 THEN SALDO.SALDO4 ELSE
                        CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =6 THEN SALDO.SALDO5 ELSE
                          CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =7 THEN SALDO.SALDO6 ELSE
                            CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =8 THEN SALDO.SALDO7 ELSE
                              CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =9 THEN SALDO.SALDO8 ELSE
                                CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =10 THEN SALDO.SALDO9 ELSE
                                  CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =11 THEN SALDO.SALDO10 ELSE
                                    CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =12 THEN SALDO.SALDO11 ELSE
                                      CASE WHEN TO_NUMBER(TO_CHAR(PCK_ENTORNO.FC_GETFECHAINICIAL,'MM')) =13 THEN SALDO.SALDO12 ELSE
                                      0
                                      END
                                    END
                                  END
                                END
                              END
                            END
                          END
                        END
                      END
                    END
                  END
                END
              END SALDO_ANT,
              0 VALOR_DEBITO, 
              0 VALOR_CREDITO
         FROM PLAN_CONTABLE INNER JOIN SALDO_AUX_CONTABLE SALDO
          ON  SALDO.COMPANIA      = PLAN_CONTABLE.COMPANIA 
          AND SALDO.ANO           = PLAN_CONTABLE.ANO 
          AND SALDO.CODIGO        = PLAN_CONTABLE.CODIGO
        INNER JOIN CENTRO_COSTO 
          ON  SALDO.COMPANIA      = CENTRO_COSTO.COMPANIA 
          AND SALDO.ANO           = CENTRO_COSTO.ANO 
          AND SALDO.CENTRO_COSTO  = CENTRO_COSTO.CODIGO
        INNER JOIN TERCERO 
         ON  SALDO.COMPANIA  = TERCERO.COMPANIA 
         AND SALDO.TERCERO   = TERCERO.NIT 
         AND SALDO.SUCURSAL  = TERCERO.SUCURSAL   
        INNER JOIN AUXILIAR 
          ON  SALDO.COMPANIA   = AUXILIAR.COMPANIA 
          AND SALDO.ANO        = AUXILIAR.ANO 
          AND SALDO.AUXILIAR   = AUXILIAR.CODIGO  
        INNER JOIN REFERENCIA 
          ON  SALDO.COMPANIA    = REFERENCIA.COMPANIA 
          AND SALDO.ANO         = REFERENCIA.ANO 
          AND SALDO.REFERENCIA  = REFERENCIA.CODIGO  
        INNER JOIN FUENTE_RECURSOS 
          ON  SALDO.COMPANIA         = FUENTE_RECURSOS.COMPANIA 
          AND SALDO.ANO              = FUENTE_RECURSOS.ANO 
          AND SALDO.FUENTE_RECURSO   = FUENTE_RECURSOS.CODIGO   
         WHERE SALDO.COMPANIA= PCK_ENTORNO.FC_GETCOMPANIA
           AND SALDO.ANO     = PCK_ENTORNO.FC_GETANIOINICIAL
           AND SALDO.CODIGO  BETWEEN PCK_ENTORNO.FC_GETCODIGOINICIAL AND PCK_ENTORNO.FC_GETCODIGOFINAL 
           AND NOT EXISTS (SELECT 'X'
                           FROM PLAN_CONTABLE PLAN INNER JOIN DETALLE_COMPROBANTE_CNT DETFRACC
                            ON  DETFRACC.COMPANIA = PLAN.COMPANIA 
                            AND DETFRACC.ANO      = PLAN.ANO 
                            AND DETFRACC.CUENTA   = PLAN.CODIGO
                           WHERE PLAN.COMPANIA= PCK_ENTORNO.FC_GETCOMPANIA
                            AND PLAN.ANO      = PCK_ENTORNO.FC_GETANIOINICIAL
                            AND PLAN.CODIGO  BETWEEN PCK_ENTORNO.FC_GETCODIGOINICIAL AND PCK_ENTORNO.FC_GETCODIGOFINAL 
                            AND DETFRACC.TIPO_CPTE BETWEEN 'AAA' AND 'ZZZZ' 
                            AND DETFRACC.FECHA BETWEEN PCK_ENTORNO.FC_GETFECHAINICIAL AND PCK_ENTORNO.FC_GETFECHAFINAL
                            AND SALDO.COMPANIA       = PLAN.COMPANIA 
                            AND SALDO.ANO            = PLAN.ANO
                            AND SALDO.CODIGO         = PLAN.CODIGO
                            AND SALDO.CENTRO_COSTO   = CASE WHEN MAN_CEN_CTO IN(0) THEN '99999999999999999999' ELSE DETFRACC.CENTRO_COSTO   END
                            AND SALDO.TERCERO        = CASE WHEN MAN_AUX_TER IN(0) THEN '999999999999999999'   ELSE DETFRACC.TERCERO        END
                            AND SALDO.SUCURSAL       = CASE WHEN MAN_AUX_TER IN(0) THEN '999'                  ELSE DETFRACC.SUCURSAL       END
                            AND SALDO.AUXILIAR       = CASE WHEN MAN_AUX_GEN IN(0) THEN '99999999999999999999' ELSE DETFRACC.AUXILIAR       END
                            AND SALDO.REFERENCIA     = CASE WHEN MAN_AUX_REF IN(0) THEN '99999999999999999999' ELSE DETFRACC.REFERENCIA     END
                            AND SALDO.FUENTE_RECURSO = CASE WHEN MAN_AUX_FUE IN(0) THEN '99999999999999999999' ELSE DETFRACC.FUENTE_RECURSO END                   
        )
  ) SIN_DET
  WHERE SIN_DET.SALDO_ANT<>0
 
  
;