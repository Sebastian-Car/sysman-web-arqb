CREATE OR REPLACE FORCE EDITIONABLE VIEW "V_QRY_RETENCIONES_DS" ("COMPANIA", "ANO", "TIPO", "NUMERO", "TIPORETENCION", "CODIGORETENCION", "VALOR", "VALORBASE", "NOM_TIPORETENCION", "NOMBRE_RETENCION", "CUENTA_DEBITO", "CUENTA_CREDITO", "PCT_APLICAR") AS 
  SELECT 
COMPROBANTE_CNTRETENCION.COMPANIA, 
COMPROBANTE_CNTRETENCION.ANO, 
COMPROBANTE_CNTRETENCION.TIPO, 
COMPROBANTE_CNTRETENCION.NUMERO, 
COMPROBANTE_CNTRETENCION.TIPORETENCION, 
COMPROBANTE_CNTRETENCION.CODIGORETENCION, 
COMPROBANTE_CNTRETENCION.VALOR, 
COMPROBANTE_CNTRETENCION.VALORBASE, 
TIPORETENCION.NOMBRE AS NOM_TIPORETENCION, 
RETENCIONES.NOMBRE AS NOMBRE_RETENCION,
RETENCIONES.CUENTA_DEBITO, 
RETENCIONES.CUENTA_CREDITO, 
RETENCIONES.PCT_APLICAR
FROM 
TIPO_COMPROBANTE 
INNER JOIN COMPROBANTE_CNTRETENCION 
ON TIPO_COMPROBANTE.COMPANIA = COMPROBANTE_CNTRETENCION.COMPANIA 
AND TIPO_COMPROBANTE.CODIGO = COMPROBANTE_CNTRETENCION.TIPO 
INNER JOIN TIPORETENCION 
ON COMPROBANTE_CNTRETENCION.TIPORETENCION = TIPORETENCION.CODIGO 
INNER JOIN RETENCIONES 
ON COMPROBANTE_CNTRETENCION.COMPANIA = RETENCIONES.COMPANIA 
AND COMPROBANTE_CNTRETENCION.ANO = RETENCIONES.ANO 
AND COMPROBANTE_CNTRETENCION.TIPORETENCION = RETENCIONES.TIPO
AND COMPROBANTE_CNTRETENCION.CODIGORETENCION = RETENCIONES.CODIGO
WHERE
TIPO_COMPROBANTE.DOCSOPORTE Not In (0);