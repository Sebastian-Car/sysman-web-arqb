       DETALLE_COMPROBANTE_CNT.CMPTE_AFECTADO CMPTE_AFECTADO_DET,  
       DETALLE_COMPROBANTE_CNT.TIPO_CPTE_AFECT TIPO_CPTE_AFECT_DET,
       COMPROBANTE_CNT.CREATED_BY,
       COMPROBANTE_CNT.FECHAPAGADOGN,
       COMPROBANTE_CNT.ORDENADOR
FROM COMPROBANTE_CNT  
 INNER JOIN TIPO_COMPROBANTE 
    ON  COMPROBANTE_CNT.COMPANIA  = TIPO_COMPROBANTE.COMPANIA 
    AND COMPROBANTE_CNT.TIPO      = TIPO_COMPROBANTE.CODIGO    
 LEFT JOIN DETALLE_COMPROBANTE_CNT 
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA    = COMPROBANTE_CNT.COMPANIA 
    AND DETALLE_COMPROBANTE_CNT.ANO         = COMPROBANTE_CNT.ANO 
    AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE   = COMPROBANTE_CNT.TIPO 
    AND DETALLE_COMPROBANTE_CNT.COMPROBANTE = COMPROBANTE_CNT.NUMERO
  LEFT JOIN PLAN_CONTABLE
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA 
    AND DETALLE_COMPROBANTE_CNT.ANO      = PLAN_CONTABLE.ANO 
    AND DETALLE_COMPROBANTE_CNT.CUENTA   = PLAN_CONTABLE.CODIGO
  
  INNER JOIN CENTRO_COSTO CENTRO_COSTO_COM
   ON  COMPROBANTE_CNT.COMPANIA     = CENTRO_COSTO_COM.COMPANIA
   AND COMPROBANTE_CNT.ANO          = CENTRO_COSTO_COM.ANO 
   AND COMPROBANTE_CNT.CENTRO_COSTO = CENTRO_COSTO_COM.CODIGO   
   
  LEFT JOIN CENTRO_COSTO 
   ON  DETALLE_COMPROBANTE_CNT.COMPANIA                                                  = CENTRO_COSTO.COMPANIA 
   AND DETALLE_COMPROBANTE_CNT.ANO                                                       = CENTRO_COSTO.ANO 
   AND CASE WHEN MAN_CEN_CTO IN(0) THEN '99999999999999999999' ELSE DETALLE_COMPROBANTE_CNT.CENTRO_COSTO END = CENTRO_COSTO.CODIGO
   
  LEFT JOIN CENTRO_COSTO CENTRO_COSTO_DET 
   ON  DETALLE_COMPROBANTE_CNT.COMPANIA     = CENTRO_COSTO_DET.COMPANIA 
   AND DETALLE_COMPROBANTE_CNT.ANO          = CENTRO_COSTO_DET.ANO 
   AND DETALLE_COMPROBANTE_CNT.CENTRO_COSTO = CENTRO_COSTO_DET.CODIGO 
   
  INNER JOIN TERCERO TERCERO_COM
   ON  COMPROBANTE_CNT.COMPANIA = TERCERO_COM.COMPANIA 
   AND COMPROBANTE_CNT.TERCERO  = TERCERO_COM.NIT 
   AND COMPROBANTE_CNT.SUCURSAL = TERCERO_COM.SUCURSAL  
   
  LEFT JOIN TERCERO 
   ON  DETALLE_COMPROBANTE_CNT.COMPANIA                                                                = TERCERO.COMPANIA 
   AND CASE WHEN MAN_AUX_TER IN(0) THEN '999999999999999999' ELSE DETALLE_COMPROBANTE_CNT.TERCERO  END = TERCERO.NIT 
   AND CASE WHEN MAN_AUX_TER IN(0) THEN '999'                ELSE DETALLE_COMPROBANTE_CNT.SUCURSAL END = TERCERO.SUCURSAL    
   
  LEFT JOIN DEPARTAMENTO 
    ON  TERCERO.PAIS         = DEPARTAMENTO.PAIS 
    AND TERCERO.DEPARTAMENTO = DEPARTAMENTO.CODIGO 
  LEFT JOIN CIUDAD 
    ON  TERCERO.PAIS         = CIUDAD.PAIS 
    AND TERCERO.DEPARTAMENTO = CIUDAD.DEPARTAMENTO 
    AND TERCERO.CIUDAD       = CIUDAD.CODIGO 
    
  LEFT JOIN TERCERO TERCERO_DET
   ON  DETALLE_COMPROBANTE_CNT.COMPANIA = TERCERO_DET.COMPANIA 
   AND DETALLE_COMPROBANTE_CNT.TERCERO  = TERCERO_DET.NIT 
   AND DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO_DET.SUCURSAL   
   
  
  INNER JOIN AUXILIAR AUXILIAR_COM
    ON  COMPROBANTE_CNT.COMPANIA    = AUXILIAR_COM.COMPANIA 
  	AND COMPROBANTE_CNT.ANO         = AUXILIAR_COM.ANO 
    AND COMPROBANTE_CNT.AUXILIAR    = AUXILIAR_COM.CODIGO   
    
  LEFT JOIN AUXILIAR 
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA                                              = AUXILIAR.COMPANIA 
  	AND DETALLE_COMPROBANTE_CNT.ANO                                                   = AUXILIAR.ANO 
    AND CASE WHEN MAN_AUX_GEN IN(0) THEN '99999999999999999999' ELSE DETALLE_COMPROBANTE_CNT.AUXILIAR END = AUXILIAR.CODIGO   
    
  LEFT JOIN AUXILIAR AUXILIAR_DET
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA = AUXILIAR_DET.COMPANIA 
  	AND DETALLE_COMPROBANTE_CNT.ANO      = AUXILIAR_DET.ANO 
    AND DETALLE_COMPROBANTE_CNT.AUXILIAR = AUXILIAR_DET.CODIGO    

  INNER JOIN REFERENCIA REFERENCIA_COM
    ON  COMPROBANTE_CNT.COMPANIA   = REFERENCIA_COM.COMPANIA 
  	AND COMPROBANTE_CNT.ANO        = REFERENCIA_COM.ANO 
    AND COMPROBANTE_CNT.REFERENCIA = REFERENCIA_COM.CODIGO   

  LEFT JOIN REFERENCIA 
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA                                                = REFERENCIA.COMPANIA 
  	AND DETALLE_COMPROBANTE_CNT.ANO                                                     = REFERENCIA.ANO 
    AND CASE WHEN MAN_AUX_REF IN(0) THEN '99999999999999999999' ELSE DETALLE_COMPROBANTE_CNT.REFERENCIA END = REFERENCIA.CODIGO    
    
  LEFT JOIN REFERENCIA REFERENCIA_DET
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA   = REFERENCIA_DET.COMPANIA 
  	AND DETALLE_COMPROBANTE_CNT.ANO        = REFERENCIA_DET.ANO 
    AND DETALLE_COMPROBANTE_CNT.REFERENCIA = REFERENCIA_DET.CODIGO
  
  INNER JOIN FUENTE_RECURSOS FUENTE_RECURSOS_COM 
    ON  COMPROBANTE_CNT.COMPANIA        = FUENTE_RECURSOS_COM.COMPANIA 
  	AND COMPROBANTE_CNT.ANO             = FUENTE_RECURSOS_COM.ANO 
    AND COMPROBANTE_CNT.FUENTE_RECURSO  = FUENTE_RECURSOS_COM.CODIGO    
    
  LEFT JOIN FUENTE_RECURSOS 
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA                                                    = FUENTE_RECURSOS.COMPANIA 
  	AND DETALLE_COMPROBANTE_CNT.ANO                                                         = FUENTE_RECURSOS.ANO 
    AND CASE WHEN MAN_AUX_FUE IN(0) THEN '99999999999999999999' ELSE DETALLE_COMPROBANTE_CNT.FUENTE_RECURSO END = FUENTE_RECURSOS.CODIGO    
    
  LEFT JOIN FUENTE_RECURSOS FUENTE_RECURSOS_DET
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA = FUENTE_RECURSOS_DET.COMPANIA 
  	AND DETALLE_COMPROBANTE_CNT.ANO      = FUENTE_RECURSOS_DET.ANO 
    AND DETALLE_COMPROBANTE_CNT.FUENTE_RECURSO = FUENTE_RECURSOS_DET.CODIGO 
