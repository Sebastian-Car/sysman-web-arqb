CREATE OR REPLACE VIEW V_BASE_TRANSACCIONESALMACEN AS 
SELECT 
  D_MOVIMIENTO.COMPANIA, 
  TIPOMOVIMIENTO.CONCEPTO, 
  D_MOVIMIENTO.TIPOMOVIMIENTO, 
  D_MOVIMIENTO.MOVIMIENTO, 
  MOVIMIENTO.TERCERO, 
  MOVIMIENTO.SUCURSAL, 
  TERCERO.NOMBRE AS NOMBRETERCERO, 
  D_MOVIMIENTO.ELEMENTO, 
  INVENTARIO.NOMBRELARGO AS NOMBREELEMENTO, 
  D_MOVIMIENTO.CODIGO, 
CASE WHEN TIPOMOVIMIENTO.CLASE='E' OR TIPOMOVIMIENTO.CLASE='T' THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END AS ENTRADAS,  
CASE WHEN TIPOMOVIMIENTO.CLASE='S' OR TIPOMOVIMIENTO.CLASE='T' THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END AS SALIDAS,  
CASE WHEN TIPOMOVIMIENTO.CONCEPTO='CR' AND D_MOVIMIENTO.VALORTOTAL>=0 THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END AS AJUSTESDEBITO, 
CASE WHEN TIPOMOVIMIENTO.CONCEPTO='CR' AND D_MOVIMIENTO.VALORTOTAL<0 THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END AS AJUSTESCREDITO, 
CASE WHEN TIPOMOVIMIENTO.CONCEPTO='CR' AND D_MOVIMIENTO.CANTIDAD>=0 THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END AS CANTIDADAJUSTESDEBITO,
CASE WHEN TIPOMOVIMIENTO.CONCEPTO='CR' AND D_MOVIMIENTO.CANTIDAD<0 THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END AS CANTIDADAJUSTESCREDITO,
CASE WHEN TIPOMOVIMIENTO.CLASE='E' OR TIPOMOVIMIENTO.CLASE='T' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END AS VALOR_ENTRADAS,
CASE WHEN TIPOMOVIMIENTO.CLASE='S' OR TIPOMOVIMIENTO.CLASE='T' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END AS VALOR_SALIDAS,
  MOVIMIENTO.BODEGA_ORIGEN, 
  MOVIMIENTO.BODEGA_DESTINO, 
  TIPOMOVIMIENTO.CLASE, 
  D_MOVIMIENTO.FECHA, 
  D_MOVIMIENTO.HORA, 
  BODEGA.NOMBRE AS NOMBREBODEGA_ORIGEN, 
  BODEGA_1.NOMBRE AS NOMBREBODEGA_DESTINO, 
  MOVIMIENTO.DEPENDENCIA_ORIGEN, 
  MOVIMIENTO.DEPENDENCIA_DESTINO, 
  DEPENDENCIA.NOMBRE AS NOMBRE_DEPENDENCIA_ORIGEN, 
  DEPENDENCIA_1.NOMBRE AS NOMBRE_DEPENDENCIA_DESTINO, 
  MOVIMIENTO.RESPONSABLE_ORIGEN, 
  MOVIMIENTO.SUCURSAL_RESORIGEN, 
  MOVIMIENTO.RESPONSABLE_DESTINO, 
  MOVIMIENTO.SUCURSAL_RESDESTINO, 
  TERCERO_1.NOMBRE AS NOMBRE_RESPONSABLE_ORIGEN, 
  TERCERO_2.NOMBRE AS NOMBRE_RESPONSABLE_DESTINO, 
  D_MOVIMIENTO.SERIE, 
  D_MOVIMIENTO.AREA,
  D_MOVIMIENTO.NUMEROCATASTRAL,
  D_MOVIMIENTO.ESCRITURA,
  D_MOVIMIENTO.MATINMOBILIARIA,
  D_MOVIMIENTO.COD_CHIP
FROM D_MOVIMIENTO 
INNER JOIN MOVIMIENTO 
    ON D_MOVIMIENTO.COMPANIA = MOVIMIENTO.COMPANIA 
    AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO 
    AND D_MOVIMIENTO.MOVIMIENTO = MOVIMIENTO.NUMERO 
INNER JOIN TIPOMOVIMIENTO 
    ON D_MOVIMIENTO.COMPANIA = TIPOMOVIMIENTO.COMPANIA 
    AND D_MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO 
INNER JOIN BODEGA 
    ON MOVIMIENTO.COMPANIA = BODEGA.COMPANIA 
    AND MOVIMIENTO.BODEGA_ORIGEN = BODEGA.CODIGO 
INNER JOIN DEPENDENCIA 
    ON MOVIMIENTO.COMPANIA = DEPENDENCIA.COMPANIA 
    AND MOVIMIENTO.DEPENDENCIA_ORIGEN = DEPENDENCIA.CODIGO 
INNER JOIN BODEGA BODEGA_1 
    ON MOVIMIENTO.COMPANIA = BODEGA_1.COMPANIA 
    AND MOVIMIENTO.BODEGA_DESTINO = BODEGA_1.CODIGO 
INNER JOIN DEPENDENCIA DEPENDENCIA_1 
    ON MOVIMIENTO.COMPANIA = DEPENDENCIA_1.COMPANIA 
    AND MOVIMIENTO.DEPENDENCIA_DESTINO = DEPENDENCIA_1.CODIGO 
INNER JOIN TERCERO 
    ON MOVIMIENTO.COMPANIA = TERCERO.COMPANIA  
    AND MOVIMIENTO.TERCERO = TERCERO.NIT 
    AND MOVIMIENTO.SUCURSAL = TERCERO.SUCURSAL 
INNER JOIN INVENTARIO 
    ON D_MOVIMIENTO.COMPANIA = INVENTARIO.COMPANIA 
    AND D_MOVIMIENTO.ELEMENTO = INVENTARIO.CODIGOELEMENTO 
INNER JOIN TERCERO TERCERO_1
    ON MOVIMIENTO.COMPANIA = TERCERO_1.COMPANIA  
    AND MOVIMIENTO.RESPONSABLE_ORIGEN = TERCERO_1.NIT 
    AND MOVIMIENTO.SUCURSAL_RESORIGEN = TERCERO_1.SUCURSAL 
INNER JOIN TERCERO TERCERO_2 
    ON MOVIMIENTO.COMPANIA = TERCERO_2.COMPANIA
    AND MOVIMIENTO.RESPONSABLE_DESTINO = TERCERO_2.NIT
    AND MOVIMIENTO.SUCURSAL_RESDESTINO = TERCERO_2.SUCURSAL
 
  ;