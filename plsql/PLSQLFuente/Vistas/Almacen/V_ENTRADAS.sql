CREATE OR REPLACE VIEW V_ENTRADAS AS 
SELECT ACUMU."COMPANIA",ACUMU."TIPOMOVIMIENTO",ACUMU."MOVIMIENTO",ACUMU."ELEMENTO",ACUMU."FECHA",ACUMU."CANTIDAD",ACUMU."VALORUNITARIO",ACUMU."TOTAL",ACUMU."ACUMULADOCANTIDAD",ACUMU."ACUMULADOVALOR",ACUMU."HORA",ACUMU."CODIGO", 
CASE WHEN ACUMU.CANTIDAD IN (0) THEN 0 ELSE ROUND(ACUMU.ACUMULADOVALOR/ACUMU.ACUMULADOCANTIDAD,2) END PROMEDIOACUMULADO
FROM (
SELECT DISTINCT 
       DMOV.COMPANIA, 
       DMOV.TIPOMOVIMIENTO, 
       DMOV.MOVIMIENTO, 
       DMOV.ELEMENTO, 
       DMOV.FECHA, 
       DMOV.CANTIDAD, 
       DMOV.VALORUNITARIO, 
       DMOV.CANTIDAD * DMOV.VALORUNITARIO TOTAL, 
       SUM(DMOV.CANTIDAD                      ) OVER (PARTITION BY DMOV.COMPANIA, DMOV.ELEMENTO ORDER BY DMOV.COMPANIA, DMOV.ELEMENTO, DMOV.FECHA, DMOV.HORA ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) ACUMULADOCANTIDAD,
       SUM(DMOV.CANTIDAD * DMOV.VALORUNITARIO ) OVER (PARTITION BY DMOV.COMPANIA, DMOV.ELEMENTO ORDER BY DMOV.COMPANIA, DMOV.ELEMENTO, DMOV.FECHA, DMOV.HORA ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) ACUMULADOVALOR, 
       DMOV.HORA, 
       DMOV.CODIGO
FROM D_MOVIMIENTO DMOV INNER JOIN INVENTARIO  INV 
  ON DMOV.COMPANIA = INV.COMPANIA 
 AND DMOV.ELEMENTO = INV.CODIGOELEMENTO
 INNER JOIN TIPOMOVIMIENTO TIPO 
    ON DMOV.COMPANIA       = TIPO.COMPANIA 
   AND DMOV.TIPOMOVIMIENTO = TIPO.CODIGO
WHERE INV.TIPO      ='C' 
  AND TIPO.CLASE    ='E' 
  AND DMOV.IND_REG  NOT IN (0)
  --AND DMOV.ELEMENTO='104010039'
ORDER BY DMOV.COMPANIA, DMOV.ELEMENTO, DMOV.FECHA, DMOV.HORA
) ACUMU
 
  
;
