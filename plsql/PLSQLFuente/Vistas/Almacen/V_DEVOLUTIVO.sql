CREATE OR REPLACE VIEW V_DEVOLUTIVO AS 
SELECT 
DEPENDENCIA.COMPANIA, 
INVENTARIO.NOMBRELARGO, 
DEVOLUTIVO.ELEMENTO AS CODIGOELEMENTODEV, 
DEVOLUTIVO.SERIE, 
DEVOLUTIVO.DESCRIPCION, 
DEVOLUTIVO.MARCA, 
DEVOLUTIVO.SERIEDEVOLUTIVO,
TERCERO.NIT AS RESPONSABLE, 
TERCERO.SUCURSAL, 
TERCERO.NOMBRE AS NOMBRETERCERO, 
TRIM(NOMBRELARGO) || ' MARCA:' || DEVOLUTIVO.MARCA || '-' || DEVOLUTIVO.DESCRIPCION || DECODE(TRIM(SERIEDEVOLUTIVO),'','','SERIE:' || SERIEDEVOLUTIVO) AS NOMBRELARGOS, 
DEVOLUTIVO.VALOR AS VALORDEVOLUTIVO, 
DEVOLUTIVO.SERIEANTERIOR, 
DEVOLUTIVO.DEPENDENCIA,
DEPENDENCIA.NOMBRE AS NOMBREDEPENDENCIA, 
DEVOLUTIVO.FECHAADQUISICION
FROM DEVOLUTIVO 
INNER JOIN INVENTARIO 
  ON DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA 
  AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
INNER JOIN DEPENDENCIA 
  ON DEVOLUTIVO.COMPANIA = DEPENDENCIA.COMPANIA 
  AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO 
INNER JOIN TERCERO 
  ON DEVOLUTIVO.COMPANIA = TERCERO.COMPANIA 
  AND DEVOLUTIVO.RESPONSABLE = TERCERO.NIT 
  AND DEVOLUTIVO.SUCURSAL_RESPONSABLE = TERCERO.SUCURSAL
 
  
 ;
