CREATE OR REPLACE FORCE EDITIONABLE VIEW "V_BASE_CUENTAALMACEN" ("TIPO_FIC", "CLASE_BODEGA_ORIGEN", "CLASE_BODEGA_DESTINO", "COMPANIA", "TIPOMOVIMIENTO", "MOVIMIENTO", "ELEMENTO", "CODIGO", "IND_REG", "CONCEPTO", "TIPO", "TERCERO", "SUCURSALTERCERO", "NOMBRETERCERO", "NOMBREELEMENTO", "UNIDAD", "CUENTA", "TIPOGRUPO", "GRUPO", "NOMBREGRUPO", "ENTRADAS", "SALIDAS", "VALOR_ENTRADAS", "VALOR_SALIDAS", "AJUSTESDEBITO", "AJUSTESCREDITO", "CANTIDADAJUSTESDEBITO", "CANTIDADAJUSTESCREDITO", "BODEGA_DESTINO1", "CLASE", "FECHA", "HORA", "NOMBREBODEGA", "CLASEFINAL", "DEPENDENCIA_DESTINO", "RESPONSABLE_ORIGEN", "SUCURSAL_RESORIGEN", "NOMBRE_RESPONSABLE_ORIGEN", "NOMBRE_DEPENDENCIA", "SERIE", "SUCURSAL_RESDESTINO", "SALDOCANT") AS
SELECT DATOS.TIPO_FIC,DATOS.CLASE_BODEGA_ORIGEN,DATOS.CLASE_BODEGA_DESTINO,DATOS.COMPANIA,DATOS.TIPOMOVIMIENTO,DATOS.MOVIMIENTO,DATOS.ELEMENTO,DATOS.CODIGO,DATOS.IND_REG,DATOS.CONCEPTO,DATOS.TIPO,DATOS.TERCERO,DATOS.SUCURSALTERCERO,DATOS.NOMBRETERCERO,DATOS.NOMBREELEMENTO,DATOS.UNIDAD,DATOS.CUENTA,DATOS.TIPOGRUPO,DATOS.GRUPO,DATOS.NOMBREGRUPO,DATOS.ENTRADAS,DATOS.SALIDAS,DATOS.VALOR_ENTRADAS,DATOS.VALOR_SALIDAS,DATOS.AJUSTESDEBITO,DATOS.AJUSTESCREDITO,DATOS.CANTIDADAJUSTESDEBITO,DATOS.CANTIDADAJUSTESCREDITO,DATOS.BODEGA_DESTINO1,DATOS.CLASE,DATOS.FECHA,DATOS.HORA,DATOS.NOMBREBODEGA,DATOS.CLASEFINAL,DATOS.DEPENDENCIA_DESTINO,DATOS.RESPONSABLE_ORIGEN,DATOS.SUCURSAL_RESORIGEN,DATOS.NOMBRE_RESPONSABLE_ORIGEN,DATOS.NOMBRE_DEPENDENCIA,DATOS.SERIE,DATOS.SUCURSAL_RESDESTINO, DATOS.SALDOCANT
FROM (
      SELECT
      '1' TIPO_FIC,
      MOVIMIENTO.CLASE_BODEGA_ORIGEN,
      MOVIMIENTO.CLASE_BODEGA_DESTINO,
      D_MOVIMIENTO.COMPANIA,
      D_MOVIMIENTO.TIPOMOVIMIENTO,
      D_MOVIMIENTO.MOVIMIENTO,
      D_MOVIMIENTO.ELEMENTO,
      D_MOVIMIENTO.CODIGO,
      D_MOVIMIENTO.IND_REG,
      TIPOMOVIMIENTO.CONCEPTO,
      INVENTARIOELEMENTO.TIPO,
      TERCERO.NIT AS TERCERO,
      TERCERO.SUCURSAL AS SUCURSALTERCERO,
      TERCERO.NOMBRE AS NOMBRETERCERO,
      INVENTARIOELEMENTO.NOMBRELARGO AS NOMBREELEMENTO,
      INVENTARIOELEMENTO.UNIDAD,
      CASE WHEN CASE WHEN TIPOMOVIMIENTO.CLASE = 'S' THEN MOVIMIENTO.CLASE_BODEGA_ORIGEN ELSE MOVIMIENTO.CLASE_BODEGA_DESTINO END IN('20') THEN
          CASE WHEN BODEGA.MANTENIMIENTO NOT IN(0) THEN NVL(INVENTARIOPADRE.CUENTAACTIVOMAN, INVENTARIOPADRE.CUENTAACTIVO)
          ELSE CASE WHEN BODEGA.NO_EXPLOTADOS NOT IN(0)
                    THEN NVL(INVENTARIOPADRE.CUENTAACTIVONEXP, INVENTARIOPADRE.CUENTAACTIVO)
                    ELSE  INVENTARIOPADRE.CUENTAACTIVO
                    END
          END
      ELSE
      CASE WHEN CASE WHEN TIPOMOVIMIENTO.CLASE = 'S' THEN MOVIMIENTO.CLASE_BODEGA_ORIGEN ELSE MOVIMIENTO.CLASE_BODEGA_DESTINO END IN('30') THEN INVENTARIOPADRE.CUENTAACTIVOS ELSE
      CASE WHEN CASE WHEN TIPOMOVIMIENTO.CLASE = 'S'  THEN MOVIMIENTO.CLASE_BODEGA_ORIGEN ELSE MOVIMIENTO.CLASE_BODEGA_DESTINO END IN('40') THEN INVENTARIOPADRE.CUENTAACTIVOI ELSE
      CASE WHEN CASE WHEN TIPOMOVIMIENTO.CLASE = 'S'  THEN MOVIMIENTO.CLASE_BODEGA_ORIGEN ELSE MOVIMIENTO.CLASE_BODEGA_DESTINO END IN('50') THEN INVENTARIOPADRE.CUENTAACTIVOR ELSE
      CASE WHEN CASE WHEN TIPOMOVIMIENTO.CLASE = 'S'  THEN MOVIMIENTO.CLASE_BODEGA_ORIGEN ELSE MOVIMIENTO.CLASE_BODEGA_DESTINO END IN('70') THEN INVENTARIOPADRE.CUENTAACTIVOS ELSE
      CASE WHEN CASE WHEN TIPOMOVIMIENTO.CLASE = 'S'  THEN MOVIMIENTO.CLASE_BODEGA_ORIGEN ELSE MOVIMIENTO.CLASE_BODEGA_DESTINO END IN('90') THEN INVENTARIOPADRE.CUENTAACTIVOC ELSE
      '' END END END END END END CUENTA,
      INVENTARIOPADRE.TIPO AS TIPOGRUPO,
        INVENTARIOPADRE.CODIGOELEMENTO AS GRUPO,
        INVENTARIOPADRE.NOMBRELARGO AS NOMBREGRUPO,
      CASE WHEN TIPOMOVIMIENTO.CLASE='E' OR TIPOMOVIMIENTO.CLASE='T' OR TIPOMOVIMIENTO.CLASE='D' THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END
      - CASE WHEN TIPOMOVIMIENTO.CLASE='T' AND TIPOMOVIMIENTO.CONCEPTO='T' THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END AS ENTRADAS,
      CASE WHEN TIPOMOVIMIENTO.CLASE='S' OR TIPOMOVIMIENTO.CLASE='T'  THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END
      - CASE WHEN TIPOMOVIMIENTO.CLASE='T' AND TIPOMOVIMIENTO.CONCEPTO='T' THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END
      - CASE WHEN TIPOMOVIMIENTO.CLASE='T' AND TIPOMOVIMIENTO.CONCEPTO='DS' THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END
      - CASE WHEN TIPOMOVIMIENTO.CLASE='T' AND TIPOMOVIMIENTO.CONCEPTO='L' THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END
      AS SALIDAS,
      CASE WHEN TIPOMOVIMIENTO.CLASE='E' AND TIPOMOVIMIENTO.CONCEPTO='CR' THEN 0 ELSE
      CASE WHEN TIPOMOVIMIENTO.CLASE='E' OR TIPOMOVIMIENTO.CLASE='T' OR TIPOMOVIMIENTO.CLASE='D' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END
      - CASE WHEN TIPOMOVIMIENTO.CLASE='T' AND TIPOMOVIMIENTO.CONCEPTO='T' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END END AS VALOR_ENTRADAS,
      CASE WHEN TIPOMOVIMIENTO.CLASE='S' AND TIPOMOVIMIENTO.CONCEPTO='CR' THEN 0 ELSE
      CASE WHEN TIPOMOVIMIENTO.CLASE='S' OR TIPOMOVIMIENTO.CLASE='T' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END
      - CASE WHEN TIPOMOVIMIENTO.CLASE='T' AND TIPOMOVIMIENTO.CONCEPTO='T' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END
      - CASE WHEN TIPOMOVIMIENTO.CLASE='T' AND TIPOMOVIMIENTO.CONCEPTO='DS' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END
      - CASE WHEN TIPOMOVIMIENTO.CLASE='T' AND TIPOMOVIMIENTO.CONCEPTO='L' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END END
      AS VALOR_SALIDAS,
      CASE WHEN TIPOMOVIMIENTO.CONCEPTO='CR' AND ((TIPOMOVIMIENTO.CLASE='E'AND D_MOVIMIENTO.VALORTOTAL>=0) OR (TIPOMOVIMIENTO.CLASE='S'AND D_MOVIMIENTO.VALORTOTAL<=0)) THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END AS AJUSTESDEBITO,
      CASE WHEN TIPOMOVIMIENTO.CONCEPTO='CR' AND ((TIPOMOVIMIENTO.CLASE='E'AND D_MOVIMIENTO.VALORTOTAL<=0) OR (TIPOMOVIMIENTO.CLASE='S'AND D_MOVIMIENTO.VALORTOTAL>=0)) THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END AS AJUSTESCREDITO,
      CASE WHEN TIPOMOVIMIENTO.CONCEPTO='CR' AND D_MOVIMIENTO.CANTIDAD>=0 THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END AS CANTIDADAJUSTESDEBITO,
      CASE WHEN TIPOMOVIMIENTO.CONCEPTO='CR' AND D_MOVIMIENTO.CANTIDAD<0 THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END AS CANTIDADAJUSTESCREDITO,
      BODEGA.CODIGO AS BODEGA_DESTINO1,
      TIPOMOVIMIENTO.CLASE,
      D_MOVIMIENTO.FECHA,
      D_MOVIMIENTO.HORA,
      BODEGA.NOMBRE AS NOMBREBODEGA,
      BODEGA.CLASE_BODEGA AS CLASEFINAL,
      DEPENDENCIA.CODIGO  AS DEPENDENCIA_DESTINO,
      MOVIMIENTO.RESPONSABLE_ORIGEN,
      MOVIMIENTO.SUCURSAL_RESORIGEN,
      TERCERO_1.NOMBRE AS NOMBRE_RESPONSABLE_ORIGEN,
      DEPENDENCIA.NOMBRE AS NOMBRE_DEPENDENCIA,
      D_MOVIMIENTO.SERIE,
      MOVIMIENTO.SUCURSAL_RESDESTINO,
      D_MOVIMIENTO.SALDOCANT
      FROM MOVIMIENTO
      INNER JOIN TIPOMOVIMIENTO
            ON MOVIMIENTO.COMPANIA = TIPOMOVIMIENTO.COMPANIA
            AND MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
      INNER JOIN BODEGA
            ON MOVIMIENTO.COMPANIA = BODEGA.COMPANIA
            AND CASE WHEN TIPOMOVIMIENTO.CLASE = 'S' THEN MOVIMIENTO.BODEGA_ORIGEN ELSE MOVIMIENTO.BODEGA_DESTINO END = BODEGA.CODIGO
      INNER JOIN DEPENDENCIA
            ON MOVIMIENTO.COMPANIA = DEPENDENCIA.COMPANIA
            AND CASE WHEN TIPOMOVIMIENTO.CLASE = 'S' THEN MOVIMIENTO.DEPENDENCIA_ORIGEN ELSE MOVIMIENTO.DEPENDENCIA_DESTINO END = DEPENDENCIA.CODIGO
      INNER JOIN D_MOVIMIENTO
            ON D_MOVIMIENTO.COMPANIA = MOVIMIENTO.COMPANIA
            AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO
            AND D_MOVIMIENTO.MOVIMIENTO = MOVIMIENTO.NUMERO
      INNER JOIN INVENTARIO INVENTARIOELEMENTO
            ON D_MOVIMIENTO.COMPANIA = INVENTARIOELEMENTO.COMPANIA
            AND D_MOVIMIENTO.ELEMENTO = INVENTARIOELEMENTO.CODIGOELEMENTO
      INNER JOIN TERCERO
            ON MOVIMIENTO.COMPANIA = TERCERO.COMPANIA
            AND MOVIMIENTO.TERCERO = TERCERO.NIT
            AND MOVIMIENTO.SUCURSAL = TERCERO.SUCURSAL
      INNER JOIN TERCERO TERCERO_1
            ON MOVIMIENTO.COMPANIA = TERCERO_1.COMPANIA
            AND MOVIMIENTO.RESPONSABLE_ORIGEN = TERCERO_1.NIT
            AND MOVIMIENTO.SUCURSAL_RESORIGEN = TERCERO_1.SUCURSAL
      LEFT JOIN
      ( SELECT INVENTARIO.COMPANIA, INVENTARIO.CODIGOELEMENTO, CUENTAACTIVO,
               CUENTAACTIVOI, CUENTAACTIVOR, CUENTAACTIVOS, CUENTAACTIVOC, NOMBRELARGO, TIPO
               ,CUENTAACTIVOMAN, CUENTAACTIVONEXP
        FROM INVENTARIO
        INNER JOIN (
            SELECT COMPANIA, MAX(TO_NUMBER(VALOR)) ANCHO
        FROM (
        SELECT COMPANIA, FECHA_INICIAL, VALOR, 1 ORDEN
          FROM   PARAMETRO
          WHERE UPPER(TRIM(NOMBRE))   = UPPER('DIGITOS AGRUPACION INVENTARIO')
          AND MODULO    IN(10)
          AND FECHA_INICIAL < = SYSDATE
        UNION
        SELECT COMPANIA, FECHA_INICIAL, VALOR, 2 ORDEN
          FROM   PARAMETRO
          WHERE UPPER(TRIM(NOMBRE))   = UPPER('DIGITOS AGRUPACION INVENTARIO')
          AND MODULO    IN(-1)
          AND FECHA_INICIAL < = SYSDATE
          ORDER BY ORDEN , FECHA_INICIAL DESC
        ) TABLA
    GROUP BY COMPANIA
            ) PAR
        ON PAR.COMPANIA = INVENTARIO.COMPANIA
        AND LENGTH(INVENTARIO.CODIGOELEMENTO)= PAR.ANCHO
      ) INVENTARIOPADRE
      ON D_MOVIMIENTO.COMPANIA = INVENTARIOPADRE.COMPANIA
      AND SUBSTR(D_MOVIMIENTO.ELEMENTO,1,LENGTH(INVENTARIOPADRE.CODIGOELEMENTO)) = INVENTARIOPADRE.CODIGOELEMENTO
    UNION ALL
      SELECT
      '2' TIPO_FIC,
      MOVIMIENTO.CLASE_BODEGA_ORIGEN,
      MOVIMIENTO.CLASE_BODEGA_DESTINO,
      D_MOVIMIENTO.COMPANIA,
      D_MOVIMIENTO.TIPOMOVIMIENTO,
      D_MOVIMIENTO.MOVIMIENTO,
      D_MOVIMIENTO.ELEMENTO,
      D_MOVIMIENTO.CODIGO,
      D_MOVIMIENTO.IND_REG,
      TIPOMOVIMIENTO.CONCEPTO,
      INVENTARIOELEMENTO.TIPO,
      TERCERO.NIT AS TERCERO,
      TERCERO.SUCURSAL AS SUCURSALTERCERO,
      TERCERO.NOMBRE AS NOMBRETERCERO,
      INVENTARIOELEMENTO.NOMBRELARGO AS NOMBREELEMENTO,
      INVENTARIOELEMENTO.UNIDAD,
      CASE WHEN CASE WHEN TIPOMOVIMIENTO.CLASE = 'S' THEN MOVIMIENTO.CLASE_BODEGA_DESTINO ELSE MOVIMIENTO.CLASE_BODEGA_ORIGEN END IN('20') THEN
          CASE WHEN BODEGA.MANTENIMIENTO NOT IN(0) THEN NVL(INVENTARIOPADRE.CUENTAACTIVOMAN, INVENTARIOPADRE.CUENTAACTIVO)
          ELSE CASE WHEN BODEGA.NO_EXPLOTADOS NOT IN(0)
                    THEN NVL(INVENTARIOPADRE.CUENTAACTIVONEXP, INVENTARIOPADRE.CUENTAACTIVO)
                    ELSE  INVENTARIOPADRE.CUENTAACTIVO
                    END
          END
      ELSE
      CASE WHEN CASE WHEN TIPOMOVIMIENTO.CLASE = 'S' THEN MOVIMIENTO.CLASE_BODEGA_DESTINO ELSE MOVIMIENTO.CLASE_BODEGA_ORIGEN END IN('30') THEN INVENTARIOPADRE.CUENTAACTIVOS ELSE
      CASE WHEN CASE WHEN TIPOMOVIMIENTO.CLASE = 'S'  THEN MOVIMIENTO.CLASE_BODEGA_DESTINO ELSE MOVIMIENTO.CLASE_BODEGA_ORIGEN END IN('40') THEN INVENTARIOPADRE.CUENTAACTIVOI ELSE
      CASE WHEN CASE WHEN TIPOMOVIMIENTO.CLASE = 'S'  THEN MOVIMIENTO.CLASE_BODEGA_DESTINO ELSE MOVIMIENTO.CLASE_BODEGA_ORIGEN END IN('50') THEN INVENTARIOPADRE.CUENTAACTIVOR ELSE
      CASE WHEN CASE WHEN TIPOMOVIMIENTO.CLASE = 'S'  THEN MOVIMIENTO.CLASE_BODEGA_DESTINO ELSE MOVIMIENTO.CLASE_BODEGA_ORIGEN END IN('70') THEN INVENTARIOPADRE.CUENTAACTIVOS ELSE
      CASE WHEN CASE WHEN TIPOMOVIMIENTO.CLASE = 'S'  THEN MOVIMIENTO.CLASE_BODEGA_DESTINO ELSE MOVIMIENTO.CLASE_BODEGA_ORIGEN END IN('90') THEN INVENTARIOPADRE.CUENTAACTIVOC ELSE
      '' END END END END END END CUENTA,
      INVENTARIOPADRE.TIPO AS TIPOGRUPO,
        INVENTARIOPADRE.CODIGOELEMENTO AS GRUPO,
        INVENTARIOPADRE.NOMBRELARGO AS NOMBREGRUPO,
      CASE WHEN TIPOMOVIMIENTO.CLASE='S' OR TIPOMOVIMIENTO.CLASE='T' THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END
      - CASE WHEN TIPOMOVIMIENTO.CLASE='T' AND TIPOMOVIMIENTO.CONCEPTO='DS' THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END
      - CASE WHEN TIPOMOVIMIENTO.CLASE='T' AND TIPOMOVIMIENTO.CONCEPTO='T' THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END
      - CASE WHEN TIPOMOVIMIENTO.CLASE='T' AND TIPOMOVIMIENTO.CONCEPTO='L' THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END AS ENTRADAS,
      CASE WHEN TIPOMOVIMIENTO.CLASE='E' OR TIPOMOVIMIENTO.CLASE='T' OR TIPOMOVIMIENTO.CLASE='D' THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END
      - CASE WHEN TIPOMOVIMIENTO.CLASE='T' AND TIPOMOVIMIENTO.CONCEPTO='T' THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END AS SALIDAS,
      CASE WHEN TIPOMOVIMIENTO.CLASE='S' OR TIPOMOVIMIENTO.CLASE='T' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END
      - CASE WHEN TIPOMOVIMIENTO.CLASE='T' AND TIPOMOVIMIENTO.CONCEPTO='T' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END
      - CASE WHEN TIPOMOVIMIENTO.CLASE='T' AND TIPOMOVIMIENTO.CONCEPTO='DS' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END
      - CASE WHEN TIPOMOVIMIENTO.CLASE='T' AND TIPOMOVIMIENTO.CONCEPTO='L' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END AS VALOR_ENTRADAS,
      CASE WHEN TIPOMOVIMIENTO.CLASE='E' AND TIPOMOVIMIENTO.CONCEPTO='CR' THEN 0 ELSE
      CASE WHEN TIPOMOVIMIENTO.CLASE='E' OR TIPOMOVIMIENTO.CLASE='T' OR TIPOMOVIMIENTO.CLASE='D' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END
      - CASE WHEN TIPOMOVIMIENTO.CLASE='T' AND TIPOMOVIMIENTO.CONCEPTO='T' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END END AS VALOR_SALIDAS,
      CASE WHEN TIPOMOVIMIENTO.CONCEPTO='CR' AND D_MOVIMIENTO.VALORTOTAL>=0 THEN 0 ELSE 0 END AS AJUSTESDEBITO,
      CASE WHEN TIPOMOVIMIENTO.CONCEPTO='CR' AND D_MOVIMIENTO.VALORTOTAL<0 THEN 0 ELSE 0 END AS AJUSTESCREDITO,
      CASE WHEN TIPOMOVIMIENTO.CONCEPTO='CR' AND D_MOVIMIENTO.CANTIDAD>=0 THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END AS CANTIDADAJUSTESDEBITO,
      CASE WHEN TIPOMOVIMIENTO.CONCEPTO='CR' AND D_MOVIMIENTO.CANTIDAD<0 THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END AS CANTIDADAJUSTESCREDITO,
      BODEGA.CODIGO AS BODEGA_DESTINO1,
      CASE WHEN TIPOMOVIMIENTO.CLASE='E' THEN 'S' ELSE CASE WHEN TIPOMOVIMIENTO.CLASE='S' THEN 'E' ELSE TIPOMOVIMIENTO.CLASE END END AS CLASE,
      D_MOVIMIENTO.FECHA,
      D_MOVIMIENTO.HORA,
      BODEGA.NOMBRE AS NOMBREBODEGA,
      BODEGA.CLASE_BODEGA AS CLASEFINAL,
      DEPENDENCIA.CODIGO AS DEPENDENCIA_DESTINO,
      MOVIMIENTO.RESPONSABLE_DESTINO AS RESPONSABLE_ORIGEN,
      MOVIMIENTO.SUCURSAL_RESORIGEN,
      TERCERO_1.NOMBRE AS NOMBRE_RESPONSABLE_DESTINO,
      DEPENDENCIA.NOMBRE AS NOMBRE_DEPENDENCIA,
      D_MOVIMIENTO.SERIE,
      MOVIMIENTO.SUCURSAL_RESDESTINO,
      D_MOVIMIENTO.SALDOCANT
      FROM D_MOVIMIENTO
      INNER JOIN MOVIMIENTO
            ON D_MOVIMIENTO.COMPANIA = MOVIMIENTO.COMPANIA
            AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO
            AND D_MOVIMIENTO.MOVIMIENTO = MOVIMIENTO.NUMERO
      INNER JOIN TIPOMOVIMIENTO
            ON D_MOVIMIENTO.COMPANIA = TIPOMOVIMIENTO.COMPANIA
            AND D_MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
      INNER JOIN BODEGA
            ON MOVIMIENTO.COMPANIA = BODEGA.COMPANIA
            AND CASE WHEN TIPOMOVIMIENTO.CLASE = 'S' THEN MOVIMIENTO.BODEGA_DESTINO ELSE MOVIMIENTO.BODEGA_ORIGEN END = BODEGA.CODIGO
      INNER JOIN DEPENDENCIA
            ON MOVIMIENTO.COMPANIA = DEPENDENCIA.COMPANIA
            AND CASE WHEN TIPOMOVIMIENTO.CLASE = 'S' THEN MOVIMIENTO.DEPENDENCIA_DESTINO ELSE MOVIMIENTO.DEPENDENCIA_ORIGEN END = DEPENDENCIA.CODIGO
      INNER JOIN TERCERO
            ON MOVIMIENTO.COMPANIA = TERCERO.COMPANIA
            AND MOVIMIENTO.TERCERO = TERCERO.NIT
            AND MOVIMIENTO.SUCURSAL = TERCERO.SUCURSAL
      INNER JOIN TERCERO TERCERO_1
            ON MOVIMIENTO.COMPANIA = TERCERO_1.COMPANIA
            AND MOVIMIENTO.RESPONSABLE_DESTINO = TERCERO_1.NIT
            AND MOVIMIENTO.SUCURSAL_RESDESTINO = TERCERO_1.SUCURSAL
      INNER JOIN INVENTARIO INVENTARIOELEMENTO
            ON D_MOVIMIENTO.COMPANIA = INVENTARIOELEMENTO.COMPANIA
            AND D_MOVIMIENTO.ELEMENTO = INVENTARIOELEMENTO.CODIGOELEMENTO
       LEFT JOIN
       ( SELECT INVENTARIO.COMPANIA, INVENTARIO.CODIGOELEMENTO, CUENTAACTIVO,
               CUENTAACTIVOI, CUENTAACTIVOR, CUENTAACTIVOS, CUENTAACTIVOC, NOMBRELARGO, TIPO
               ,CUENTAACTIVOMAN, CUENTAACTIVONEXP
        FROM INVENTARIO
        INNER JOIN (
            SELECT COMPANIA, MAX(TO_NUMBER(VALOR)) ANCHO
        FROM (
        SELECT COMPANIA, FECHA_INICIAL, VALOR, 1 ORDEN
          FROM   PARAMETRO
          WHERE UPPER(TRIM(NOMBRE))   = UPPER('DIGITOS AGRUPACION INVENTARIO')
          AND MODULO    IN(10)
          AND FECHA_INICIAL < = SYSDATE
        UNION
        SELECT COMPANIA, FECHA_INICIAL, VALOR, 2 ORDEN
          FROM   PARAMETRO
          WHERE UPPER(TRIM(NOMBRE))   = UPPER('DIGITOS AGRUPACION INVENTARIO')
          AND MODULO    IN(-1)
          AND FECHA_INICIAL < = SYSDATE
          ORDER BY ORDEN , FECHA_INICIAL DESC
        ) TABLA
    GROUP BY COMPANIA
            ) PAR
        ON PAR.COMPANIA = INVENTARIO.COMPANIA
        AND LENGTH(INVENTARIO.CODIGOELEMENTO)= PAR.ANCHO
      ) INVENTARIOPADRE
      ON D_MOVIMIENTO.COMPANIA = INVENTARIOPADRE.COMPANIA
      AND SUBSTR(D_MOVIMIENTO.ELEMENTO,1,LENGTH(INVENTARIOPADRE.CODIGOELEMENTO)) = INVENTARIOPADRE.CODIGOELEMENTO
   ) DATOS;
   COMMENT ON COLUMN "V_BASE_CUENTAALMACEN"."COMPANIA" IS 'Código de la compañía a la que pertenece el registro';
   COMMENT ON COLUMN "V_BASE_CUENTAALMACEN"."TERCERO" IS 'identificar del tercero relacionado al registro, usualmente corresponde al documento de identificación';
