CREATE OR REPLACE VIEW V_MOVIMIENTO_INFORME AS 
	SELECT MOVIMIENTO.COMPANIA,
       MOVIMIENTO.TIPOMOVIMIENTO,
       MOVIMIENTO.NUMERO MOVIMIENTO,       
       TIPOMOVIMIENTO.NOMBRE TIPOMOVIMIENTO_NOM,
       TIPOMOVIMIENTO.NOMBRECORTO TIPOMOVIMIENTO_NOMCORTO,
       TIPOMOVIMIENTO.TIPOPESONA,
       DECODE(TIPOMOVIMIENTO.TIPOPESONA,'P','Proveedor','T','Tercero','R','Responsable',' ') TIPOPERSONA_NOM,
       TIPOMOVIMIENTO.CLASE,
       DECODE(TIPOMOVIMIENTO.CLASE,'E','Entrada','S','Salida','T','Traspaso','D','Descargo',' ') CLASE_NOM,
       D_MOVIMIENTO.ELEMENTO,
       D_MOVIMIENTO.SERIE,
       D_MOVIMIENTO.SERIEDEVOLUTIVO,
       D_MOVIMIENTO.FECHA FECHA_D_MOV,
       D_MOVIMIENTO.HORA  HORA_D_MOV,
       D_MOVIMIENTO.ESPECIFICACION, 
       D_MOVIMIENTO.MARCA,
       D_MOVIMIENTO.ESCRITURA,
       D_MOVIMIENTO.NUMEROCATASTRAL,
       D_MOVIMIENTO.MATINMOBILIARIA,
       D_MOVIMIENTO.COD_CHIP,
       D_MOVIMIENTO.IND_REG,
       D_MOVIMIENTO.VALORUNITARIO,
       D_MOVIMIENTO.VALORTOTAL VALORTOTAL_D,
       MOVIMIENTO.VALORTOTAL VALORTOTAL_MOV,
       D_MOVIMIENTO.CANTIDAD,
       D_MOVIMIENTO.AJUSTECENTAVOS AJUSTECENTAVOS_D,
       MOVIMIENTO.AJUSTECENTAVOS AJUSTECENTAVOS_MOV,
       MOVIMIENTO.AJUSTECENTAVOS1 AJUSTECENTAVOS1_MOV,
       MOVIMIENTO.TOTALCONAJUSTE TOTALCONAJUSTE_MOV,
       MOVIMIENTO.AJUSTE_DEC_MANUAL,
       MOVIMIENTO.DESCRIPCION DESCRIPCION_MOV ,
       MOVIMIENTO.FECHA FECHA_MOV,
       MOVIMIENTO.DEPENDENCIA_ORIGEN,
       DEPENDENCIAORIGEN.NOMBRE DEPENDENCIA_ORIGEN_NOM, 
       MOVIMIENTO.RESPONSABLE_ORIGEN,
       RESPONSABLE_ORIGEN.CARGO RESPONSABLE_ORIGEN_CARGO,
       RESPONSABLE_ORIGEN_NOM.NOMBRE RESPONSABLE_ORIGEN_NOM,
       RESPONSABLE_ORIGEN_NOM.NIT RESPONSABLE_ORIGEN_NIT,
       RESPONSABLE_ORIGEN_NOM.TIPOID RESPONSABLE_ORIGEN_TIPO,
       MOVIMIENTO.DEPENDENCIA_DESTINO,
       DEPENDENCIADESTINO.NOMBRE DEPENDENCIA_DESTINO_NOM,
       MOVIMIENTO.RESPONSABLE_DESTINO,
       MOVIMIENTO.SUCURSAL_RESDESTINO,
       RESPONSABLE_DESTINO.CARGO RESPONSABLE_DESTINO_CARGO,
       RESPONSABLE_DESTINO_NOM.NOMBRE RESPONSABLE_DESTINO_NOM,
       RESPONSABLE_DESTINO_NOM.NIT RESPONSABLE_DESTINO_NIT,
       RESPONSABLE_DESTINO_NOM.TIPOID RESPONSABLE_DESTINO_TIPO,
       MOVIMIENTO.NIT_RECIBIDO RECIBIDO_POR,
       MOVIMIENTO.SUCURSAL_RECIBIDO ,
       RECIBIDOPOR.NOMBRE NOMBRE_RECIBIDOPOR,
       MOVIMIENTO.TERCERO,
       TERCERO.NOMBRE TERCERO_NOM,
       TERCERO.TELEFONOS TERCERO_TEL,
       TERCERO.FAX TERCERO_FAX,
       TERCERO.DIRECCION TERCERO_DIR,
       MOVIMIENTO.PROVEEDORCA PROVEEDOR,
       PROVEEDOR.NOMBRE PROVEEDOR_NOM,
       PROVEEDOR.TELEFONOS PROVEEDOR_TEL,
       PROVEEDOR.FAX PROVEEDOR_FAX,       
       PROVEEDOR.DIRECCION PROVEEDOR_DIR,
       INVENTARIO.NOMBRELARGO,
       INVENTARIO.NOMBRELARGO || ' ' || INVENTARIO.DESCRIPCION DESCRIPCION_INV,       
       INVENTARIO.UNIDAD,
       UNIDAD.NOMBRE UNIDAD_NOM,
       MOVIMIENTO.CENTRODECOSTO,
       CENTRO_COSTO.NOMBRE CENTRODECOSTO_NOM,
       MOVIMIENTO.FECHAMOVASOCIADO,
       MOVIMIENTO.MOVASOCIADO,
       MOVIMIENTO.TIPOMOVASOCIADO,
       DOCASOCIADO.NOMBRE TIPOMOVASOCIADO_NOM,
       MOVIMIENTO.VALORDOCASOCIADO,
       MOVIMIENTO.FACTURANO,
       MOVIMIENTO.REMISIONNO,
       MOVIMIENTO.FINICIO_CONTRATO,
       D_MOVIMIENTO.CODIGO,
       D_MOVIMIENTO.ORDENDESUMINISTRO,
       TIPOMOVIMIENTO.CONCEPTO,
       D_MOVIMIENTO.PORCDESCUENTO,
       D_MOVIMIENTO.CLASEDESCARGO,
       D_MOVIMIENTO.NIIF_VIDA_UTIL,
       TIPOMOVIMIENTO.TIPOELEMENTO,
       MOVIMIENTO.CREATED_BY,
       MOVIMIENTO.MODIFIED_BY,
	D_MOVIMIENTO.REFERENCIA,
	D_MOVIMIENTO.FUENTEDERECURSO,
    D_MOVIMIENTO.MODELO
FROM TIPOMOVIMIENTO INNER JOIN MOVIMIENTO 
  ON TIPOMOVIMIENTO.COMPANIA = MOVIMIENTO.COMPANIA
 AND TIPOMOVIMIENTO.CODIGO   = MOVIMIENTO.TIPOMOVIMIENTO
LEFT JOIN D_MOVIMIENTO 
        ON D_MOVIMIENTO.COMPANIA       = MOVIMIENTO.COMPANIA
       AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO 
       AND D_MOVIMIENTO.MOVIMIENTO     = MOVIMIENTO.NUMERO
LEFT JOIN INVENTARIO 
        ON INVENTARIO.COMPANIA        = D_MOVIMIENTO.COMPANIA 
       AND INVENTARIO.CODIGOELEMENTO  = D_MOVIMIENTO.ELEMENTO 
LEFT JOIN  UNIDAD 
       ON  UNIDAD.UNIDAD     = INVENTARIO.UNIDAD
LEFT JOIN TERCERO 
        ON TERCERO.COMPANIA = MOVIMIENTO.COMPANIA
       AND TERCERO.SUCURSAL = MOVIMIENTO.SUCURSAL
       AND TERCERO.NIT      = MOVIMIENTO.TERCERO
LEFT JOIN TERCERO PROVEEDOR
        ON PROVEEDOR.COMPANIA = MOVIMIENTO.COMPANIA
       AND PROVEEDOR.SUCURSAL = MOVIMIENTO.SUCURSALCA
       AND PROVEEDOR.NIT      = MOVIMIENTO.PROVEEDORCA 
LEFT JOIN TERCERO RECIBIDOPOR
        ON RECIBIDOPOR.COMPANIA = MOVIMIENTO.COMPANIA
       AND RECIBIDOPOR.NIT      = MOVIMIENTO.NIT_RECIBIDO
       AND RECIBIDOPOR.SUCURSAL = MOVIMIENTO.SUCURSAL_RECIBIDO
LEFT JOIN CENTRO_COSTO
        ON MOVIMIENTO.COMPANIA      = CENTRO_COSTO.COMPANIA
       AND MOVIMIENTO.ANO           = CENTRO_COSTO.ANO
       AND MOVIMIENTO.CENTRODECOSTO = CENTRO_COSTO.CODIGO
LEFT JOIN  DEPENDENCIA DEPENDENCIAORIGEN
        ON DEPENDENCIAORIGEN.COMPANIA  = MOVIMIENTO.COMPANIA
       AND DEPENDENCIAORIGEN.CODIGO    = MOVIMIENTO.DEPENDENCIA_ORIGEN
LEFT JOIN  DEPENDENCIA_RESPONSABLE RESPONSABLE_ORIGEN
        ON RESPONSABLE_ORIGEN.COMPANIA     = MOVIMIENTO.COMPANIA
       AND RESPONSABLE_ORIGEN.DEPENDENCIA  = MOVIMIENTO.DEPENDENCIA_ORIGEN
       AND RESPONSABLE_ORIGEN.RESPONSABLE  = MOVIMIENTO.RESPONSABLE_ORIGEN      
       AND RESPONSABLE_ORIGEN.SUCURSAL     = MOVIMIENTO.SUCURSAL_RESORIGEN      
LEFT JOIN  TERCERO RESPONSABLE_ORIGEN_NOM
        ON RESPONSABLE_ORIGEN_NOM.COMPANIA = RESPONSABLE_ORIGEN.COMPANIA
       AND RESPONSABLE_ORIGEN_NOM.SUCURSAL = RESPONSABLE_ORIGEN.SUCURSAL
       AND RESPONSABLE_ORIGEN_NOM.NIT      = RESPONSABLE_ORIGEN.RESPONSABLE 
LEFT JOIN  DEPENDENCIA DEPENDENCIADESTINO
        ON DEPENDENCIADESTINO.COMPANIA  = MOVIMIENTO.COMPANIA
       AND DEPENDENCIADESTINO.CODIGO    = MOVIMIENTO.DEPENDENCIA_DESTINO
LEFT JOIN  DEPENDENCIA_RESPONSABLE RESPONSABLE_DESTINO
        ON RESPONSABLE_DESTINO.COMPANIA     = MOVIMIENTO.COMPANIA
       AND RESPONSABLE_DESTINO.DEPENDENCIA  = MOVIMIENTO.DEPENDENCIA_DESTINO
       AND RESPONSABLE_DESTINO.RESPONSABLE  = MOVIMIENTO.RESPONSABLE_DESTINO     
       AND RESPONSABLE_DESTINO.SUCURSAL     = MOVIMIENTO.SUCURSAL_RESDESTINO      
LEFT JOIN  TERCERO RESPONSABLE_DESTINO_NOM
        ON RESPONSABLE_DESTINO_NOM.COMPANIA = RESPONSABLE_DESTINO.COMPANIA
       AND RESPONSABLE_DESTINO_NOM.SUCURSAL = RESPONSABLE_DESTINO.SUCURSAL
       AND RESPONSABLE_DESTINO_NOM.NIT      = RESPONSABLE_DESTINO.RESPONSABLE 
LEFT JOIN DOCASOCIADO 
        ON DOCASOCIADO.CODIGO        = MOVIMIENTO.TIPOMOVASOCIADO;
        
COMMENT ON COLUMN "V_MOVIMIENTO_INFORME"."COMPANIA" IS 'Codigo de la compania a la que pertenece el registro';
COMMENT ON COLUMN "V_MOVIMIENTO_INFORME"."TERCERO" IS 'identificar del tercero relacionado al registro, usualmente corresponde al documento de identificacion';
COMMENT ON COLUMN "V_MOVIMIENTO_INFORME"."CREATED_BY" IS 'Campo de auditoria que almacena el codigo del usuario que creo el registro';  
COMMENT ON COLUMN "V_MOVIMIENTO_INFORME"."MODIFIED_BY" IS 'Campo de auditoria que almacena el codigo del usuario que modifico el registro por ultima vez';

