CREATE OR REPLACE VIEW V_MOVIMIENTOS_TOTALES AS 
SELECT CASE WHEN CLASE_BODEGA_ORIGEN = 20      AND CLASE_BODEGA_DESTINO<>20        THEN 'S' ELSE 
       CASE WHEN CLASE_BODEGA_ORIGEN <>20      AND CLASE_BODEGA_DESTINO=20         THEN 'E' ELSE 
       CASE WHEN CLASE_BODEGA_ORIGEN IN(40,50) AND CLASE_BODEGA_DESTINO IN(60)     THEN 'D' ELSE 
       CASE WHEN CLASE_BODEGA_ORIGEN = CLASE_BODEGA_DESTINO OR 
                (CLASE_BODEGA_ORIGEN = 30      AND CLASE_BODEGA_DESTINO IN(40,50)) THEN 'T' ELSE 'O' END
       END 
END
END CLASE, 
DEP_ORI.NOMBRE DEPENDENCIA_ORIGEN_NOM,
DEP_DES.NOMBRE DEPENDENCIA_DESTINO_NOM,
TER_ORI.NOMBRE RESPONSABLE_ORIGEN_NOM,
TER_DES.NOMBRE RESPONSABLE_DESTINO_NOM,
MOVIMIENTO."COMPANIA",MOVIMIENTO."TIPOMOVIMIENTO",MOVIMIENTO."NUMERO",MOVIMIENTO."TERCERO",MOVIMIENTO."SUCURSAL",MOVIMIENTO."CONCEPTO",MOVIMIENTO."FECHA",MOVIMIENTO."RESPONSABLE_ORIGEN",MOVIMIENTO."SUCURSAL_RESORIGEN",MOVIMIENTO."RESPONSABLE_DESTINO",MOVIMIENTO."SUCURSAL_RESDESTINO",MOVIMIENTO."BODEGA_ORIGEN",MOVIMIENTO."BODEGA_DESTINO",MOVIMIENTO."HORA",MOVIMIENTO."DESCRIPCION",MOVIMIENTO."PORCIVAGLOBAL",MOVIMIENTO."TIPOMOVASOCIADO",MOVIMIENTO."MOVASOCIADO",MOVIMIENTO."FECHAMOVASOCIADO",MOVIMIENTO."VALORIVA",MOVIMIENTO."VALORDESCUENTO",MOVIMIENTO."VALORTOTAL",MOVIMIENTO."VACIA",MOVIMIENTO."CENTRODECOSTO",MOVIMIENTO."REMISIONNO",MOVIMIENTO."FACTURANO",MOVIMIENTO."VALORDOCASOCIADO",MOVIMIENTO."AJUSTECENTAVOS",MOVIMIENTO."AJUSTECENTAVOS1",MOVIMIENTO."REGISTRADO",MOVIMIENTO."IMPRESO",MOVIMIENTO."GENERADO",MOVIMIENTO."AUXILIAR",MOVIMIENTO."TOTALCONAJUSTE",MOVIMIENTO."PROVEEDORCA",MOVIMIENTO."SUCURSALCA",MOVIMIENTO."VALORTOTALCONIVA",MOVIMIENTO."FINICIO_CONTRATO",MOVIMIENTO."FFINAL_CONTRATO",MOVIMIENTO."AJUSTE_DEC_MANUAL",MOVIMIENTO."CREATED_BY",MOVIMIENTO."DATE_CREATED",MOVIMIENTO."MODIFIED_BY",MOVIMIENTO."DATE_MODIFIED",MOVIMIENTO."CLASE_BODEGA_ORIGEN",MOVIMIENTO."CLASE_BODEGA_DESTINO",MOVIMIENTO."DEPENDENCIA_ORIGEN",MOVIMIENTO."DEPENDENCIA_DESTINO"
FROM MOVIMIENTO LEFT JOIN DEPENDENCIA DEP_ORI 
  ON MOVIMIENTO.COMPANIA           = DEP_ORI.COMPANIA
 AND MOVIMIENTO.DEPENDENCIA_ORIGEN = DEP_ORI.CODIGO  
 LEFT JOIN DEPENDENCIA DEP_DES 
  ON MOVIMIENTO.COMPANIA            = DEP_DES.COMPANIA
 AND MOVIMIENTO.DEPENDENCIA_DESTINO = DEP_DES.CODIGO  
 LEFT JOIN TERCERO TER_ORI 
  ON MOVIMIENTO.COMPANIA           = TER_ORI.COMPANIA
 AND MOVIMIENTO.SUCURSAL_RESORIGEN = TER_ORI.SUCURSAL
 AND MOVIMIENTO.RESPONSABLE_ORIGEN = TER_ORI.NIT
 LEFT JOIN TERCERO TER_DES 
  ON MOVIMIENTO.COMPANIA            = TER_DES.COMPANIA
 AND MOVIMIENTO.SUCURSAL_RESDESTINO = TER_DES.SUCURSAL
 AND MOVIMIENTO.RESPONSABLE_DESTINO = TER_DES.NIT
 
  
;