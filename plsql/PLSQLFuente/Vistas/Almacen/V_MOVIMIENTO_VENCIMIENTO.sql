CREATE OR REPLACE FORCE EDITIONABLE VIEW "V_MOVIMIENTO_VENCIMIENTO" ("COMPANIA", "TIPOMOVIMIENTO", "MOVIMIENTO", "ELEMENTO", "NOMBRELARGO", "UNIDAD", "CANTIDAD", "REFERENCIA", "LOTE", "FECHA_VENCIMIENTO", "FUENTEDERECURSO", "SALDO", "VALORUNITARIO", "VALORTOTAL", "FECHA") AS 
  SELECT 
   D.COMPANIA,
    D.TIPOMOVIMIENTO,
    D.MOVIMIENTO,
    D.ELEMENTO,
    I.NOMBRELARGO,
    U.NOMBRE UNIDAD,
    D.CANTIDAD,
    D.REFERENCIA,
    D.LOTE,
    D.FECHA_VENCIMIENTO,
    M.FUENTEDERECURSO,
   (SUM(D.CANTIDAD) - NVL(SALIDAS.CANT_SALIDA,0)) SALDO,
   D.VLRUNITARIOPROM AS VALORUNITARIO,
   (SUM(D.CANTIDAD) - NVL(SALIDAS.CANT_SALIDA,0)) *
   D.VLRUNITARIOPROM AS VALORTOTAL,
   D.FECHA
FROM MOVIMIENTO M INNER JOIN D_MOVIMIENTO D
ON M.COMPANIA=D.COMPANIA
AND  M.TIPOMOVIMIENTO=D.TIPOMOVIMIENTO
AND  M.NUMERO=D.MOVIMIENTO
INNER JOIN INVENTARIO I
ON D.COMPANIA=I.COMPANIA
AND  D.ELEMENTO=I.CODIGOELEMENTO 
INNER JOIN UNIDAD U
ON I.UNIDAD = U.UNIDAD
INNER JOIN TIPOMOVIMIENTO
ON TIPOMOVIMIENTO.COMPANIA=D.COMPANIA
AND  TIPOMOVIMIENTO.CODIGO=D.TIPOMOVIMIENTO
 LEFT JOIN (SELECT D_MOVIMIENTO.COMPANIA, 
         D_MOVIMIENTO.TIPOMOVIMIENTO_AFECT,
         D_MOVIMIENTO.MOVIMIENTO_AFECT,
         D_MOVIMIENTO.CODIGO_AFECT,
         D_MOVIMIENTO.ELEMENTO,
         SUM(D_MOVIMIENTO.CANTIDAD) CANT_SALIDA
         FROM  D_MOVIMIENTO 
         INNER JOIN TIPOMOVIMIENTO
ON TIPOMOVIMIENTO.COMPANIA=D_MOVIMIENTO.COMPANIA
AND  TIPOMOVIMIENTO.CODIGO=D_MOVIMIENTO.TIPOMOVIMIENTO
WHERE TIPOMOVIMIENTO.CLASE IN ('S')
AND TIPOMOVIMIENTO.TIPOELEMENTO IN ('C')
GROUP BY D_MOVIMIENTO.COMPANIA, 
         D_MOVIMIENTO.TIPOMOVIMIENTO_AFECT,
         D_MOVIMIENTO.MOVIMIENTO_AFECT,
         D_MOVIMIENTO.CODIGO_AFECT,
         D_MOVIMIENTO.ELEMENTO ) SALIDAS
 ON D.COMPANIA = SALIDAS.COMPANIA
AND D.TIPOMOVIMIENTO = SALIDAS.TIPOMOVIMIENTO_AFECT
AND D.MOVIMIENTO = SALIDAS.MOVIMIENTO_AFECT
AND D.CODIGO = SALIDAS.CODIGO_AFECT
AND D.ELEMENTO = SALIDAS.ELEMENTO
WHERE I.TIPO IN('C')
AND TIPOMOVIMIENTO.CLASE IN('E')
GROUP BY D.COMPANIA, D.TIPOMOVIMIENTO, D.MOVIMIENTO, D.ELEMENTO, I.NOMBRELARGO, 
D.CANTIDAD, D.REFERENCIA, D.LOTE, D.FECHA_VENCIMIENTO, M.FUENTEDERECURSO, 
SALIDAS.CANT_SALIDA, D.VALORUNITARIO, D.VALORTOTAL, D.FECHA, U.NOMBRE, 
D.VLRUNITARIOPROM
ORDER BY  D.COMPANIA,
    D.TIPOMOVIMIENTO,
    D.MOVIMIENTO;
