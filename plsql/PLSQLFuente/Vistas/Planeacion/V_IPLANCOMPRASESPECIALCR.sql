CREATE OR REPLACE VIEW V_IPLANCOMPRASESPECIALCR AS 
SELECT 
    PLAN_DE_COMPRAS.COMPANIA, 
    PLAN_DE_COMPRAS.CODIGO AS COD, 
    PLAN_DE_COMPRAS.ANO, 
    PLAN_DE_COMPRAS.NOMBRE, 
    D_ORDENDECOMPRA.CANTIDAD, 
    D_ORDENDECOMPRA.VALORUNITARIODI, 
    D_ORDENDECOMPRA.VLRTOTAL, 
    INVENTARIO.NOMBRELARGO, 
    MIN(VLRASIGNADO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) AS PRIMERODEVLRASIGNADO, 
    MIN(VLRPROGRAMADO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) AS PRIMERODEVLRPROGRAMADO, 
    SUM(PLAN_DE_COMPRAS.VLREJECUTADO) AS SUMADEVLREJECUTADO, 
    DETALLE_PLAN_COMPRAS.CODIGO, 
    INVENTARIO.CUBS, 
    D_ORDENDECOMPRA.CLASEORDEN, 
    D_ORDENDECOMPRA.ORDENDECOMPRA, 
    D_ORDENDECOMPRA.CLASEORDEN || ' ' || D_ORDENDECOMPRA.ORDENDECOMPRA AS CONTRATO, 
    ORDENDECOMPRA.FECHA, 
    EXTRACT(YEAR FROM ORDENDECOMPRA.FECHA) AS ANOORDEN
FROM 
    (
        (PLAN_DE_COMPRAS INNER JOIN (
            DETALLE_PLAN_COMPRAS INNER JOIN INVENTARIO 
                ON DETALLE_PLAN_COMPRAS.CODIGO = INVENTARIO.CODIGOELEMENTO 
                    AND DETALLE_PLAN_COMPRAS.COMPANIA = INVENTARIO.COMPANIA) 
            ON PLAN_DE_COMPRAS.ANO = DETALLE_PLAN_COMPRAS.ANO 
                AND PLAN_DE_COMPRAS.CODIGO = DETALLE_PLAN_COMPRAS.RUBRO 
                AND PLAN_DE_COMPRAS.COMPANIA = DETALLE_PLAN_COMPRAS.COMPANIA) INNER JOIN D_ORDENDECOMPRA 
        ON DETALLE_PLAN_COMPRAS.COMPANIA = D_ORDENDECOMPRA.COMPANIA 
            AND DETALLE_PLAN_COMPRAS.CODIGO = D_ORDENDECOMPRA.ELEMENTO 
            AND DETALLE_PLAN_COMPRAS.RUBRO = D_ORDENDECOMPRA.RUBROPPTO) INNER JOIN ORDENDECOMPRA 
    ON D_ORDENDECOMPRA.COMPANIA = ORDENDECOMPRA.COMPANIA 
        AND D_ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRA.CLASEORDEN 
        AND D_ORDENDECOMPRA.ORDENDECOMPRA = ORDENDECOMPRA.NUMERO         
GROUP BY 
    PLAN_DE_COMPRAS.COMPANIA, 
    PLAN_DE_COMPRAS.CODIGO, 
    PLAN_DE_COMPRAS.ANO, 
    PLAN_DE_COMPRAS.NOMBRE, 
    D_ORDENDECOMPRA.CANTIDAD, 
    D_ORDENDECOMPRA.VALORUNITARIODI, 
    D_ORDENDECOMPRA.VLRTOTAL, 
    INVENTARIO.NOMBRELARGO, 
    DETALLE_PLAN_COMPRAS.CODIGO, 
    INVENTARIO.CUBS, 
    D_ORDENDECOMPRA.CLASEORDEN, 
    D_ORDENDECOMPRA.ORDENDECOMPRA, 
    D_ORDENDECOMPRA.CLASEORDEN || ' ' || D_ORDENDECOMPRA.ORDENDECOMPRA,
    ORDENDECOMPRA.FECHA, 
    EXTRACT(YEAR FROM ORDENDECOMPRA.FECHA);