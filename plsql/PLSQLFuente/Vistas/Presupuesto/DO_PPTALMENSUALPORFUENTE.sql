CREATE OR REPLACE FORCE EDITIONABLE VIEW "DO_PPTALMENSUALPORFUENTE" ("COMPANIA", "ANO", "MES", "MES_NOMBRE", "NIVEL", "CODIGO", "ID", "NOMBRE", "FUENTE_RECURSO", "FUENTE_NOM", "TIPOVIGENCIA", "VIGENCIA", "DESTINO", "APROPIADO", "ADICION", "REDUCCION", "TRASLADO", "APRDEFINITIVA", "PAGOSEJECUTADOS", "SALDOAEJECUTAR") AS 
  SELECT ACTUAL.COMPANIA, ACTUAL.ANO, 
       ACTUAL.MES, 
       ACTUAL.MES_NOMBRE,
       LENGTH(ACTUAL.CODIGO) NIVEL,
       ACTUAL.CODIGO, 
       ACTUAL.ID, 
       ACTUAL.NOMBRE, 
       ACTUAL.FUENTE_RECURSO, 
       ACTUAL.FUENTE_NOM,
       ACTUAL.TIPOVIGENCIA,
       ACTUAL.VIGENCIA, 
       ACTUAL.DESTINO,
       --SUM(ACTUAL.APRDEFINITIVA) OVER (Partition BY ACTUAL.COMPANIA, ACTUAL.ANO, ACTUAL.ID ORDER BY ACTUAL.COMPANIA, ACTUAL.ANO, ACTUAL.MES, ACTUAL.ID)- ACTUAL.APRDEFINITIVA APRANTERIOR,
       SUM(ACTUAL.APROPIADO) OVER (Partition BY ACTUAL.COMPANIA, ACTUAL.ANO, ACTUAL.ID ORDER BY ACTUAL.COMPANIA, ACTUAL.ANO, ACTUAL.MES, ACTUAL.ID) APROPIADO,
       ACTUAL.ADICION, 
       ACTUAL.REDUCCION, 
       ACTUAL.TRASLADO, 
       SUM(ACTUAL.APRDEFINITIVA) OVER (Partition BY ACTUAL.COMPANIA, ACTUAL.ANO, ACTUAL.ID ORDER BY ACTUAL.COMPANIA, ACTUAL.ANO, ACTUAL.MES, ACTUAL.ID)  APRDEFINITIVA,
       SUM(ACTUAL.EJECUCIONPPT) OVER (Partition BY ACTUAL.COMPANIA, ACTUAL.ANO, ACTUAL.ID ORDER BY ACTUAL.COMPANIA, ACTUAL.ANO, ACTUAL.MES, ACTUAL.ID) PAGOSEJECUTADOS,
       SUM(ACTUAL.APRDEFINITIVA - ACTUAL.DISPONIBILIDAD) OVER (Partition BY ACTUAL.COMPANIA, ACTUAL.ANO, ACTUAL.ID ORDER BY ACTUAL.COMPANIA, ACTUAL.ANO, ACTUAL.MES, ACTUAL.ID) SALDOAEJECUTAR
FROM (
SELECT  
        SALDO_AUX_PPTAL.COMPANIA,
        SALDO_AUX_PPTAL.ANO,
        SALDO_AUX_PPTAL.MES,
        LPAD(SALDO_AUX_PPTAL.MES,2,0) || '-' || MES.NOMBRE MES_NOMBRE,
        PLAN_PRESUPUESTAL.CODIGO, 
        TRIM(RPAD(PLAN_PRESUPUESTAL.CODIGO,32)
          || RPAD(CASE WHEN PINI.MAN_CEN_CTO IN(0) THEN ' ' ELSE SALDO_AUX_PPTAL.CENTRO_COSTO   END  ,20) 
          || RPAD(CASE WHEN PINI.MAN_AUX_TER IN(0) THEN ' ' ELSE SALDO_AUX_PPTAL.TERCERO        END  ,18) 
          || RPAD(CASE WHEN PINI.MAN_AUX_TER IN(0) THEN ' ' ELSE SALDO_AUX_PPTAL.SUCURSAL       END  ,3) 
          || RPAD(CASE WHEN PINI.MAN_AUX_GEN IN(0) THEN ' ' ELSE SALDO_AUX_PPTAL.AUXILIAR       END  ,20) 
          || RPAD(CASE WHEN PINI.MAN_AUX_REF IN(0) THEN ' ' ELSE SALDO_AUX_PPTAL.REFERENCIA     END  ,20) 
          || RPAD(CASE WHEN PINI.MAN_AUX_FUE IN(0) THEN ' ' ELSE SALDO_AUX_PPTAL.FUENTE_RECURSO END  ,20) 
        ) ID,    
        PLAN_PRESUPUESTAL.NOMBRE ||
            CASE WHEN  PINI.MAN_AUX_TER <>0 THEN '/' || TERCERO.NOMBRE ELSE NULL END  ||
            CASE WHEN  PINI.MAN_AUX_GEN <>0 THEN '/' || AUXILIAR.NOMBRE ELSE NULL END ||
            CASE WHEN  PINI.MAN_CEN_CTO <>0 THEN '/' || CENTRO_COSTO.NOMBRE ELSE NULL END  ||
            CASE WHEN  PINI.MAN_AUX_REF <>0 THEN '/' || REFERENCIA.NOMBRE ELSE NULL END  ||
            CASE WHEN  PINI.MAN_AUX_FUE <>0 THEN '/' || FUENTE_RECURSOS.NOMBRE ELSE NULL END  
        NOMBRE,         
        SALDO_AUX_PPTAL.FUENTE_RECURSO,
        FUENTE_RECURSOS.NOMBRE FUENTE_NOM,        
        PLAN_PRESUPUESTAL.NATURALEZA,
        TIPOVIGENCIA.NOMBRE TIPOVIGENCIA,
        PLAN_PRESUPUESTAL.VIGENCIA, 
        TIPODESTINO.NOMBRE DESTINO,
        SUM(DECODE(PLAN_PRESUPUESTAL.NATURALEZA,'D',SALDO_AUX_PPTAL.APROPIACION_DEBITO  - SALDO_AUX_PPTAL.APROPIACION_CREDITO, 
                                              SALDO_AUX_PPTAL.APROPIACION_CREDITO - SALDO_AUX_PPTAL.APROPIACION_DEBITO ))
        + SUM(SALDO_AUX_PPTAL.ADICION) + SUM(SALDO_AUX_PPTAL.REDUCCION)
        + SUM(DECODE(PLAN_PRESUPUESTAL.NATURALEZA,'D',TRASLADO_DEBITO     - TRASLADO_CREDITO,    
                                              TRASLADO_CREDITO    - TRASLADO_DEBITO    ))                 APRDEFINITIVA,         
        SUM(DECODE(PLAN_PRESUPUESTAL.NATURALEZA,'D',SALDO_AUX_PPTAL.APROPIACION_DEBITO  - SALDO_AUX_PPTAL.APROPIACION_CREDITO, 
                                              SALDO_AUX_PPTAL.APROPIACION_CREDITO - SALDO_AUX_PPTAL.APROPIACION_DEBITO )) APROPIADO,        
        SUM(DECODE(PLAN_PRESUPUESTAL.NATURALEZA,'D',TRASLADO_DEBITO     - TRASLADO_CREDITO,    
                                              TRASLADO_CREDITO    - TRASLADO_DEBITO    )) TRASLADO,                                              
        SUM(SALDO_AUX_PPTAL.ADICION) ADICION,
        SUM(SALDO_AUX_PPTAL.REDUCCION) REDUCCION,
        SUM(SALDO_AUX_PPTAL.DISPONIBILIDAD) DISPONIBILIDAD,                                                       
        SUM(DECODE(PLAN_PRESUPUESTAL.NATURALEZA,'D',SALDO_AUX_PPTAL.EJE_PPT_DEBITO      - SALDO_AUX_PPTAL.EJE_PPT_CREDITO,     
                                              SALDO_AUX_PPTAL.EJE_PPT_CREDITO     - SALDO_AUX_PPTAL.EJE_PPT_DEBITO     )) EJECUCIONPPT        
FROM PLAN_PRESUPUESTAL INNER JOIN SALDO_AUX_PPTAL 
  ON PLAN_PRESUPUESTAL.COMPANIA = SALDO_AUX_PPTAL.COMPANIA
 AND PLAN_PRESUPUESTAL.ANO      = SALDO_AUX_PPTAL.ANO
 AND PLAN_PRESUPUESTAL.CODIGO   = SUBSTR(SALDO_AUX_PPTAL.CODIGO, 1, LENGTH(PLAN_PRESUPUESTAL.CODIGO)) 
 INNER JOIN MES
      ON SALDO_AUX_PPTAL.COMPANIA =  MES.COMPANIA
     AND SALDO_AUX_PPTAL.ANO      =  MES.ANO
     AND SALDO_AUX_PPTAL.MES      =  MES.NUMERO
 INNER JOIN PLAN_PRESUPUESTAL PINI
  ON SALDO_AUX_PPTAL.COMPANIA = PINI.COMPANIA
 AND SALDO_AUX_PPTAL.ANO      = PINI.ANO
 AND SALDO_AUX_PPTAL.CODIGO   = PINI.CODIGO
   INNER JOIN TERCERO 
      ON SALDO_AUX_PPTAL.COMPANIA = TERCERO.COMPANIA 
     AND SALDO_AUX_PPTAL.TERCERO  = TERCERO.NIT 
     AND SALDO_AUX_PPTAL.SUCURSAL = TERCERO.SUCURSAL

    INNER JOIN CENTRO_COSTO 
      ON SALDO_AUX_PPTAL.COMPANIA = CENTRO_COSTO.COMPANIA 
     AND SALDO_AUX_PPTAL.ANO = CENTRO_COSTO.ANO 
     AND SALDO_AUX_PPTAL.CENTRO_COSTO = CENTRO_COSTO.CODIGO

    INNER JOIN AUXILIAR 
      ON SALDO_AUX_PPTAL.COMPANIA = AUXILIAR.COMPANIA 
     AND SALDO_AUX_PPTAL.ANO = AUXILIAR.ANO 
     AND SALDO_AUX_PPTAL.AUXILIAR = AUXILIAR.CODIGO 

    INNER JOIN FUENTE_RECURSOS 
      ON SALDO_AUX_PPTAL.COMPANIA = FUENTE_RECURSOS.COMPANIA 
     AND SALDO_AUX_PPTAL.ANO = FUENTE_RECURSOS.ANO 
     AND SALDO_AUX_PPTAL.FUENTE_RECURSO = FUENTE_RECURSOS.CODIGO 

    INNER JOIN REFERENCIA 
      ON SALDO_AUX_PPTAL.COMPANIA = REFERENCIA.COMPANIA 
     AND SALDO_AUX_PPTAL.ANO = REFERENCIA.ANO 
     AND SALDO_AUX_PPTAL.REFERENCIA = REFERENCIA.CODIGO
    LEFT JOIN TIPOVIGENCIA
      ON PLAN_PRESUPUESTAL.TIPOVIGENCIA = TIPOVIGENCIA.CODIGO
     LEFT JOIN TIPODESTINO
      ON PLAN_PRESUPUESTAL.DESTINO = TIPODESTINO.CODIGO 
GROUP BY SALDO_AUX_PPTAL.COMPANIA,
        SALDO_AUX_PPTAL.ANO,
        SALDO_AUX_PPTAL.MES,
        LPAD(SALDO_AUX_PPTAL.MES,2,0) || '-' || MES.NOMBRE,
        PLAN_PRESUPUESTAL.CODIGO,
        TRIM(RPAD(PLAN_PRESUPUESTAL.CODIGO,32)
          || RPAD(CASE WHEN PINI.MAN_CEN_CTO IN(0) THEN ' ' ELSE SALDO_AUX_PPTAL.CENTRO_COSTO   END  ,20) 
          || RPAD(CASE WHEN PINI.MAN_AUX_TER IN(0) THEN ' ' ELSE SALDO_AUX_PPTAL.TERCERO        END  ,18) 
          || RPAD(CASE WHEN PINI.MAN_AUX_TER IN(0) THEN ' ' ELSE SALDO_AUX_PPTAL.SUCURSAL       END  ,3) 
          || RPAD(CASE WHEN PINI.MAN_AUX_GEN IN(0) THEN ' ' ELSE SALDO_AUX_PPTAL.AUXILIAR       END  ,20) 
          || RPAD(CASE WHEN PINI.MAN_AUX_REF IN(0) THEN ' ' ELSE SALDO_AUX_PPTAL.REFERENCIA     END  ,20) 
          || RPAD(CASE WHEN PINI.MAN_AUX_FUE IN(0) THEN ' ' ELSE SALDO_AUX_PPTAL.FUENTE_RECURSO END  ,20) 
          ) ,     

        PLAN_PRESUPUESTAL.NOMBRE ||
            CASE WHEN  PINI.MAN_AUX_TER <>0 THEN '/' || TERCERO.NOMBRE ELSE NULL END  ||
            CASE WHEN  PINI.MAN_AUX_GEN <>0 THEN '/' || AUXILIAR.NOMBRE ELSE NULL END ||
            CASE WHEN  PINI.MAN_CEN_CTO <>0 THEN '/' || CENTRO_COSTO.NOMBRE ELSE NULL END  ||
            CASE WHEN  PINI.MAN_AUX_REF <>0 THEN '/' || REFERENCIA.NOMBRE ELSE NULL END  ||
            CASE WHEN  PINI.MAN_AUX_FUE <>0 THEN '/' || FUENTE_RECURSOS.NOMBRE ELSE NULL END, 

        SALDO_AUX_PPTAL.FUENTE_RECURSO,
        FUENTE_RECURSOS.NOMBRE,
        PLAN_PRESUPUESTAL.NATURALEZA,
        TIPOVIGENCIA.NOMBRE ,
        PLAN_PRESUPUESTAL.VIGENCIA, 
        TIPODESTINO.NOMBRE
) ACTUAL;

   COMMENT ON TABLE "DO_PPTALMENSUALPORFUENTE"  IS 'Permite generar reporte de DASHONE';
