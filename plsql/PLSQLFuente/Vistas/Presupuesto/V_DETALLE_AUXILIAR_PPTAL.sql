FROM COMPROBANTE_PPTAL  
 INNER JOIN TIPO_COMPROBPP 
    ON  COMPROBANTE_PPTAL.COMPANIA  = TIPO_COMPROBPP.COMPANIA 
    AND COMPROBANTE_PPTAL.TIPO      = TIPO_COMPROBPP.CODIGO    
 LEFT JOIN DETALLE_COMPROBANTE_PPTAL 
    ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA    = COMPROBANTE_PPTAL.COMPANIA 
    AND DETALLE_COMPROBANTE_PPTAL.ANO         = COMPROBANTE_PPTAL.ANO 
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = COMPROBANTE_PPTAL.TIPO 
      AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO
  LEFT JOIN PLAN_PRESUPUESTAL
    ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA 
    AND DETALLE_COMPROBANTE_PPTAL.ANO      = PLAN_PRESUPUESTAL.ANO 
    AND DETALLE_COMPROBANTE_PPTAL.CUENTA   = PLAN_PRESUPUESTAL.CODIGO
  LEFT JOIN DEPENDENCIA DEPENDENCIA_COM
    ON  COMPROBANTE_PPTAL.COMPANIA   = DEPENDENCIA_COM.COMPANIA 
    AND COMPROBANTE_PPTAL.DEPENDENCIA = DEPENDENCIA_COM.CODIGO    
  INNER JOIN CENTRO_COSTO CENTRO_COSTO_COM
   ON  COMPROBANTE_PPTAL.COMPANIA     = CENTRO_COSTO_COM.COMPANIA
   AND COMPROBANTE_PPTAL.ANO          = CENTRO_COSTO_COM.ANO 
   AND COMPROBANTE_PPTAL.CENTRO_COSTO = CENTRO_COSTO_COM.CODIGO   

  LEFT JOIN CENTRO_COSTO 
   ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA                                                  = CENTRO_COSTO.COMPANIA 
   AND DETALLE_COMPROBANTE_PPTAL.ANO                                                       = CENTRO_COSTO.ANO 
   AND CASE WHEN MAN_CEN_CTO IN(0) THEN '99999999999999999999' ELSE DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO END = CENTRO_COSTO.CODIGO

  LEFT JOIN CENTRO_COSTO CENTRO_COSTO_DET 
   ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA     = CENTRO_COSTO_DET.COMPANIA 
   AND DETALLE_COMPROBANTE_PPTAL.ANO          = CENTRO_COSTO_DET.ANO 
   AND DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO = CENTRO_COSTO_DET.CODIGO 

  INNER JOIN TERCERO TERCERO_COM
   ON  COMPROBANTE_PPTAL.COMPANIA = TERCERO_COM.COMPANIA 
   AND COMPROBANTE_PPTAL.TERCERO  = TERCERO_COM.NIT 
   AND COMPROBANTE_PPTAL.SUCURSAL = TERCERO_COM.SUCURSAL  

  LEFT JOIN TERCERO 
   ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA                                                                = TERCERO.COMPANIA 
   AND CASE WHEN MAN_AUX_TER IN(0) THEN '999999999999999999' ELSE DETALLE_COMPROBANTE_PPTAL.TERCERO  END = TERCERO.NIT 
   AND CASE WHEN MAN_AUX_TER IN(0) THEN '999'                ELSE DETALLE_COMPROBANTE_PPTAL.SUCURSAL END = TERCERO.SUCURSAL    

  LEFT JOIN DEPARTAMENTO 
    ON  TERCERO.PAIS         = DEPARTAMENTO.PAIS 
    AND TERCERO.DEPARTAMENTO = DEPARTAMENTO.CODIGO 
  LEFT JOIN CIUDAD 
    ON  TERCERO.PAIS         = CIUDAD.PAIS 
    AND TERCERO.DEPARTAMENTO = CIUDAD.DEPARTAMENTO 
    AND TERCERO.CIUDAD       = CIUDAD.CODIGO 

  LEFT JOIN TERCERO TERCERO_DET
   ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = TERCERO_DET.COMPANIA 
   AND DETALLE_COMPROBANTE_PPTAL.TERCERO  = TERCERO_DET.NIT 
   AND DETALLE_COMPROBANTE_PPTAL.SUCURSAL = TERCERO_DET.SUCURSAL   


  INNER JOIN AUXILIAR AUXILIAR_COM
    ON  COMPROBANTE_PPTAL.COMPANIA    = AUXILIAR_COM.COMPANIA 
  	AND COMPROBANTE_PPTAL.ANO         = AUXILIAR_COM.ANO 
    AND COMPROBANTE_PPTAL.AUXILIAR    = AUXILIAR_COM.CODIGO   

  LEFT JOIN AUXILIAR 
    ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA                                              = AUXILIAR.COMPANIA 
  	AND DETALLE_COMPROBANTE_PPTAL.ANO                                                   = AUXILIAR.ANO 
    AND CASE WHEN MAN_AUX_GEN IN(0) THEN '99999999999999999999' ELSE DETALLE_COMPROBANTE_PPTAL.AUXILIAR END = AUXILIAR.CODIGO   

  LEFT JOIN AUXILIAR AUXILIAR_DET
    ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = AUXILIAR_DET.COMPANIA 
  	AND DETALLE_COMPROBANTE_PPTAL.ANO      = AUXILIAR_DET.ANO 
    AND DETALLE_COMPROBANTE_PPTAL.AUXILIAR = AUXILIAR_DET.CODIGO    

  INNER JOIN REFERENCIA REFERENCIA_COM
    ON  COMPROBANTE_PPTAL.COMPANIA   = REFERENCIA_COM.COMPANIA 
  	AND COMPROBANTE_PPTAL.ANO        = REFERENCIA_COM.ANO 
    AND COMPROBANTE_PPTAL.REFERENCIA = REFERENCIA_COM.CODIGO   

  LEFT JOIN REFERENCIA 
    ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA                                                = REFERENCIA.COMPANIA 
  	AND DETALLE_COMPROBANTE_PPTAL.ANO                                                     = REFERENCIA.ANO 
    AND CASE WHEN MAN_AUX_REF IN(0) THEN '99999999999999999999' ELSE DETALLE_COMPROBANTE_PPTAL.REFERENCIA END = REFERENCIA.CODIGO    

  LEFT JOIN REFERENCIA REFERENCIA_DET
    ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA   = REFERENCIA_DET.COMPANIA 
  	AND DETALLE_COMPROBANTE_PPTAL.ANO        = REFERENCIA_DET.ANO 
    AND DETALLE_COMPROBANTE_PPTAL.REFERENCIA = REFERENCIA_DET.CODIGO

  INNER JOIN FUENTE_RECURSOS FUENTE_RECURSOS_COM 
    ON  COMPROBANTE_PPTAL.COMPANIA        = FUENTE_RECURSOS_COM.COMPANIA 
  	AND COMPROBANTE_PPTAL.ANO             = FUENTE_RECURSOS_COM.ANO 
    AND COMPROBANTE_PPTAL.FUENTE_RECURSO  = FUENTE_RECURSOS_COM.CODIGO    

  LEFT JOIN FUENTE_RECURSOS 
    ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA                                                    = FUENTE_RECURSOS.COMPANIA 
  	AND DETALLE_COMPROBANTE_PPTAL.ANO                                                         = FUENTE_RECURSOS.ANO 
    AND CASE WHEN MAN_AUX_FUE IN(0) THEN '99999999999999999999' ELSE DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO END = FUENTE_RECURSOS.CODIGO    

  LEFT JOIN FUENTE_RECURSOS FUENTE_RECURSOS_DET
    ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = FUENTE_RECURSOS_DET.COMPANIA 
  	AND DETALLE_COMPROBANTE_PPTAL.ANO      = FUENTE_RECURSOS_DET.ANO 
    AND DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO = FUENTE_RECURSOS_DET.CODIGO

    LEFT JOIN CUIPO_CODIGO_CPC_DANE
    ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = CUIPO_CODIGO_CPC_DANE.COMPANIA 
  	AND DETALLE_COMPROBANTE_PPTAL.ANO      = CUIPO_CODIGO_CPC_DANE.ANO 
    AND DETALLE_COMPROBANTE_PPTAL.CODIGO_CPC = CUIPO_CODIGO_CPC_DANE.CODIGO;