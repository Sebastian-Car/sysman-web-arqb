                    PLAN_PRESUPUESTAL.COMPANIA = PLAN_PPTAL_CONFIG.COMPANIA
                AND PLAN_PRESUPUESTAL.ANO = PLAN_PPTAL_CONFIG.ANO
                AND PLAN_PRESUPUESTAL.CODIGO = PLAN_PPTAL_CONFIG.CODIGO
