CREATE OR REPLACE VIEW V_AFECTADOS_PPTALES AS 
(SELECT  
--* 
    AFECTADOS.COMPANIA,
    AFECTADOS.ANO,
    AFECTADOS.TIPO_CPTE TIPO_AFECTADO,
    AFECTADOS.COMPROBANTE CMPTE_AFECTADO,
    AFECTADOS.CONSECUTIVO CONS_AFECTADO,
    AFECTANTES.TIPO_CPTE TIPO_AFECTANTE,
    AFECTANTES.COMPROBANTE CMPTE_AFECTANTE,
    AFECTANTES.CONSECUTIVO CONS_AFECTANTE
FROM DETALLE_COMPROBANTE_PPTAL AFECTADOS INNER JOIN 
    (SELECT COMPANIA, ANO, TIPO_CPTE, COMPROBANTE, CONSECUTIVO, TIPO_CPTE_AFECT, CMPTE_AFECTADO, CONSECUTIVOPPTO
    FROM DETALLE_COMPROBANTE_PPTAL
    WHERE  TIPO_CPTE_AFECT IS NOT NULL
      AND CMPTE_AFECTADO IS NOT NULL) AFECTANTES
ON AFECTADOS.COMPANIA = AFECTANTES.COMPANIA
    AND AFECTADOS.ANO = AFECTANTES.ANO
    AND AFECTADOS.TIPO_CPTE = AFECTANTES.TIPO_CPTE_AFECT
    AND AFECTADOS.COMPROBANTE = AFECTANTES.CMPTE_AFECTADO
    AND AFECTADOS.CONSECUTIVO = AFECTANTES.CONSECUTIVOPPTO)
;