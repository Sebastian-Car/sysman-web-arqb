CREATE OR REPLACE VIEW V_PREDECESOR_PPTAL AS 
SELECT PREDE."COMPANIA",PREDE."ANO",PREDE."CODIGO",PREDE."ID",PREDE."NOMBRE",PREDE."PREDECESOR", PLAN_PRESUPUESTAL.NOMBRE PREDECESOR_NOMBRE
FROM (
      SELECT PLAN.COMPANIA, PLAN.ANO, PLAN.CODIGO, PLAN.ID, PLAN.NOMBRE, MAX(PRE.CODIGO) PREDECESOR
      FROM V_PLAN_PRESUPUESTAL PLAN 
        LEFT JOIN PLAN_PRESUPUESTAL PRE ON 
          PRE.COMPANIA = PLAN.COMPANIA
      AND PRE.ANO      = PLAN.ANO
      AND PRE.CODIGO   = SUBSTR(PLAN.ID,1, LENGTH(PRE.CODIGO))
      AND PRE.CODIGO   <> PLAN.ID
      GROUP BY PLAN.COMPANIA, PLAN.ANO, PLAN.CODIGO, PLAN.ID, PLAN.NOMBRE
      ) PREDE LEFT JOIN PLAN_PRESUPUESTAL
      ON PLAN_PRESUPUESTAL.COMPANIA = PREDE.COMPANIA
     AND PLAN_PRESUPUESTAL.ANO      = PREDE.ANO
     AND PLAN_PRESUPUESTAL.CODIGO   = PREDE.PREDECESOR ;