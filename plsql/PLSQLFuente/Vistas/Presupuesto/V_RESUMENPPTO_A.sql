       SUM(TOTALREGCONT                )TOTALREGCONT ,
       SUM(PACTOTAL                    )PACTOTAL,
       SUM(TOTALICA                    )TOTALICA
FROM V_RESUMENPPTO_BASE
WHERE COMPANIA =  PCK_ENTORNO.FC_GETCOMPANIA
  AND ANO      =  PCK_ENTORNO.FC_GETYEAR
  AND MES      <  PCK_ENTORNO.FC_GETMESINICIAL
  AND ID       =  PCK_ENTORNO.FC_GETRUBRO
GROUP BY COMPANIA, 
		   ANO, 
		   ID, 
		   CODIGO,
		   NATURALEZA,
		   CENTRO_COSTO,
		   TERCERO,
		   SUCURSAL,
		   AUXILIAR,
		   REFERENCIA,
		   FUENTE_RECURSO,
		   MOVIMIENTO;