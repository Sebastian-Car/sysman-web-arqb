CREATE OR REPLACE VIEW V_TOTALDISPONIBLE AS 
SELECT DIAS.COMPANIA, 
       DIAS.ELEMENTO, 
       DIAS.SERIE,
       DIAS.DIAS FECHA_INICIO,
       TO_DATE('30/12/1899 00:00:00', 'DD/MM/YYYY hh24:mi:ss') HORA,
       TO_DATE('30/12/1899 23:59:59', 'DD/MM/YYYY hh24:mi:ss') HORAFINAL,
       'NO TIENEN RESTRICCIONES' TIPO
FROM
(
WITH q(I) AS (SELECT 0
             FROM DUAL
             UNION ALL
             SELECT I+1
             FROM q
             WHERE I <1000
             )
SELECT DEV.COMPANIA, DEV.ELEMENTO, DEV.SERIE, TRUNC(SYSDATE-500+I) DIAS
FROM   q, (SELECT COMPANIA, ELEMENTO, SERIE FROM DEVOLUTIVO WHERE SE_PRESTA NOT IN(0)) DEV
 ) DIAS 
LEFT JOIN V_TOTALEXCLUIDOS
ON DIAS.COMPANIA  =V_TOTALEXCLUIDOS.COMPANIA
AND DIAS.ELEMENTO  =V_TOTALEXCLUIDOS.ELEMENTO_DEVOLUTIVO
AND DIAS.SERIE     =V_TOTALEXCLUIDOS.SERIE_DEVOLUTIVO
AND DIAS.DIAS      =V_TOTALEXCLUIDOS.FECHA_INICIO
WHERE V_TOTALEXCLUIDOS.COMPANIA IS NULL

UNION ALL

    
SELECT "COMPANIA","ELEMENTO","SERIE","FECHA_INICIO","HORA","HORAFINAL","TIPO" 
FROM (
    SELECT COMPANIA, ELEMENTO, SERIE, FECHA_INICIO, 
    CASE WHEN ANT IS NULL THEN TO_DATE('30/12/1899 00:00:00', 'DD/MM/YYYY hh24:mi:ss') ELSE SIG END HORA,
    NVL(LEAD(HORAFINAL) OVER (PARTITION BY COMPANIA, ELEMENTO, SERIE, FECHA_INICIO ORDER BY COMPANIA, ELEMENTO, SERIE, FECHA_INICIO, HORA), TO_DATE('30/12/1899 23:59:59', 'DD/MM/YYYY hh24:mi:ss')) HORAFINAL,
     'CON RESTRICCION' TIPO
    FROM(
    SELECT EXCLU.COMPANIA, 
           EXCLU.ELEMENTO_DEVOLUTIVO ELEMENTO, 
           EXCLU.SERIE_DEVOLUTIVO SERIE,
           EXCLU.FECHA_INICIO,
           HORASVALIDAS.HORA,
           HORASVALIDAS.HORAFINAL,
           LAG(HORAFINAL) OVER (PARTITION BY EXCLU.COMPANIA, EXCLU.ELEMENTO_DEVOLUTIVO, EXCLU.SERIE_DEVOLUTIVO, FECHA_INICIO 
                  ORDER BY EXCLU.COMPANIA, ELEMENTO_DEVOLUTIVO, SERIE_DEVOLUTIVO, FECHA_INICIO, HORA) ANT,
           LEAD(HORA) OVER (PARTITION BY EXCLU.COMPANIA, EXCLU.ELEMENTO_DEVOLUTIVO, EXCLU.SERIE_DEVOLUTIVO, FECHA_INICIO 
                  ORDER BY EXCLU.COMPANIA, ELEMENTO_DEVOLUTIVO, SERIE_DEVOLUTIVO, FECHA_INICIO, HORA) SIG
    FROM HORASVALIDAS INNER JOIN V_TOTALEXCLUIDOS EXCLU
      ON HORASVALIDAS.COMPANIA  = EXCLU.COMPANIA 
    WHERE (HORASVALIDAS.HORA < EXCLU.HORA_INICIO 
      OR HORASVALIDAS.HORA >=EXCLU.HORA_FIN)
      )
    WHERE ANT IS NULL
       OR SIG IS NULL
       OR HORAFINAL<>SIG
    ORDER BY COMPANIA, 
             ELEMENTO,
             SERIE,
             FECHA_INICIO, 
             HORA
  ) FIN
  WHERE FIN.HORA IS NOT NULL;