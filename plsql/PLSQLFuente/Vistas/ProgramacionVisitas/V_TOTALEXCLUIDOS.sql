CREATE OR REPLACE VIEW V_TOTALEXCLUIDOS AS 
SELECT HORARIO_EXCLUIDO.COMPANIA, 
       HORARIO_EXCLUIDO.ELEMENTO_DEVOLUTIVO,
       HORARIO_EXCLUIDO.SERIE_DEVOLUTIVO,
       TO_DATE(TO_CHAR(HORARIO_EXCLUIDO.FECHA_HORA_INICIO,'DD/MM/YYYY'),'DD/MM/YYYY') FECHA_INICIO,
       TO_DATE(TO_CHAR(HORARIO_EXCLUIDO.FECHA_HORA_FIN   ,'DD/MM/YYYY'),'DD/MM/YYYY') FECHA_FIN,
       TO_DATE('30/12/1899 ' || TO_CHAR(HORARIO_EXCLUIDO.FECHA_HORA_INICIO , 'hh24:mi:ss'),'DD/MM/YYYY  hh24:mi:ss') HORA_INICIO,
       TO_DATE('30/12/1899 ' || TO_CHAR(HORARIO_EXCLUIDO.FECHA_HORA_FIN    , 'hh24:mi:ss'),'DD/MM/YYYY  hh24:mi:ss') HORA_FIN,
       HORARIO_EXCLUIDO.FECHA_HORA_INICIO, 
       HORARIO_EXCLUIDO.FECHA_HORA_FIN  
FROM HORARIO_EXCLUIDO;