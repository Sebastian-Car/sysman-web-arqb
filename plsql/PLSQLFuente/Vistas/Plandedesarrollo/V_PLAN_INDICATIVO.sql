CREATE OR REPLACE VIEW V_PLAN_INDICATIVO AS 
SELECT   
        PI_INDICADOR.COMPANIA,
        PI_INDICADOR.VIGENCIA_INICIAL,
        PI_INDICADOR.ID_PLAN,
        PI_INDICADOR.CODIGO INDICADOR,
