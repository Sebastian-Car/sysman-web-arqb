        NVL(PCK_SYSMAN_UTL.FC_PAR(PI_INDICADOR.COMPANIA,'NOMBRE DEPARTAMENTO SIEE',52,SYSDATE,-1),' ') NOMBRE_DEPARTAMENTO,
        NVL(PCK_SYSMAN_UTL.FC_PAR(PI_INDICADOR.COMPANIA,'CODIGO MUNICIPIO SIEE',52,SYSDATE,-1),' ') CODIGO_MUNICIPIO,
        NVL(PCK_SYSMAN_UTL.FC_PAR(PI_INDICADOR.COMPANIA,'NOMBRE MUNICIPIO SIEE',52,SYSDATE,-1),' ') NOMBRE_MUNICIPIO,
        CASE WHEN PI_RELACION_PROD_RTDO.PRINCIPAL NOT IN(0) 
             THEN NVL(PI_INDICADOR.DESCRIPCION_META,' ')
          ELSE ' ' 
        END  RESULTADO,
        CASE WHEN PI_RELACION_PROD_RTDO.PRINCIPAL NOT IN(0)  
          THEN PI_INDICADOR.NOMBRE 
          ELSE ' '
        END  INDICADOR_RESULTADO,
        CASE WHEN PI_RELACION_PROD_RTDO.PRINCIPAL NOT IN(0)  
          THEN PI_INDICADOR.LB
        END  LB_RESULTADO,
        CASE WHEN PI_RELACION_PROD_RTDO.PRINCIPAL NOT IN(0)   
          THEN PI_INDICADOR.META_CUATRENIO
        END META_RESULTADO,
        NVL(PI_INDICADOR.DESCRIPCION_META,' ') PRODUCTO,
        PI_INDICADOR.NOMBRE INDICADOR_PRODUCTO,
        PI_INDICADOR.LB LB_PRODUCTO,
        PI_INDICADOR.META_CUATRENIO META_PRODUCTO,
        CASE PI_INDICADOR.TIPO_META_INDICADOR
          WHEN 'MI' THEN 'Incremento'
          WHEN 'MIA' THEN 'Incremento'
          WHEN 'MR' THEN 'Reducción'
          WHEN 'MRA' THEN 'Reducción'
          WHEN 'MM' THEN 'Mantenimiento'
        ELSE 
        ' '
        END ORIENTACION,
        PI_INDICADOR.SECTOR CODIGO_SECTOR,
        PI_INDICADOR.SECTOR || ' ' || SECTORES.DESCRIPCION SECTOR,
        PI_INDICADOR.ODS ,
        PI_INDICADOR.PILAR_MARCO,
        PI_INDICADOR_META.META,
        PI_INDICADOR_META.AVANCE,
        CASE WHEN PI_INDICADOR_META.META  NOT IN (0) THEN (PI_INDICADOR_META.AVANCE / PI_INDICADOR_META.META) * 100 ELSE 0  END EJECUTADO,
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('001')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) COFDPTO,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('002')
           THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) COFNACION,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('003')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) CREDITO,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('004')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) OTROS,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('005')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) RECURSOSPROPIOS,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('006')
           THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) SGPALIMESCOLAR,
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('007')
           THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) SGPAPSB,
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('008')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) SGPCULTURA,
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('009')
           THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) SGPDEPORTE,
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('010')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) SGPEDUCACION,
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('011')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) SGPLIBREDEST,
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('012')
           THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) SGPLIBREINV,
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('013')
           THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) SGPRIOMAGDALENA,
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('014')
           THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) SGPRIMERAINFANCIA,
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('015')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) SGPSALUD,
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('016')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) REGALIAS,
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('017')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) FUNCIONAMIENTO,
        
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('001')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) COFDPTO_OBL,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('002')
           THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) COFNACION_OBL,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('003')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) CREDITO_OBL,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('004')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) OTROSDNP_OBL,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('005')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) RECURSOSPROPIOS_OBL,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('006')
           THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) SGPALIMESCOLAR_OBL,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('007')
           THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) SGPAPSB_OBL,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('008')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) SGPCULTURA_OBL,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('009')
           THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) SGPDEPORTE_OBL,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('010')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) SGPEDUCACION_OBL,
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('011')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) SGPLIBREDEST_OBL,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('012')
           THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) SGPLIBREINV_OBL,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('013')
           THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) SGPRIOMAGDALENA_OBL,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('014')
           THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) SGPRIMERAINFANCIA_OBL,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('015')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) SGPSALUD_OBL,
        
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('016')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) REGALIAS_OBL,
        SUM(CASE WHEN FUENTE_EQUIVALENTE_DNP.CODIGO IN ('017')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES 
          ELSE 0
        END) FUNCIONAMIENTO_OBL,
        
        
        SUM(CASE WHEN TIPO_FUENTE_POAI.CODIGO IN ('1')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) LIBREDESTINACION,
        SUM(CASE WHEN TIPO_FUENTE_POAI.CODIGO IN ('2')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) DESTINACIONESP,
        SUM(CASE WHEN TIPO_FUENTE_POAI.CODIGO IN ('3')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) RENTASCEDIDAS,
         SUM(CASE WHEN TIPO_FUENTE_POAI.CODIGO IN ('4')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) SGR,
          SUM(CASE WHEN TIPO_FUENTE_POAI.CODIGO IN ('5')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) COMPENSACION,
         SUM(CASE WHEN TIPO_FUENTE_POAI.CODIGO IN ('6')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) DESARROLLO,
         SUM(CASE WHEN TIPO_FUENTE_POAI.CODIGO IN ('7')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) CTEL,
        SUM(CASE WHEN TIPO_FUENTE_POAI.CODIGO IN ('8')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) OTROSPOAI,
        SUM(CASE WHEN TIPO_FUENTE_POAI.CODIGO IN ('9')
          THEN BP_PLAN_INDICATIVO_FUENTES.PRESUPUESTO 
          ELSE 0
        END) CREDITOS,
        
        
        SUM(
        CASE
          WHEN FUENTE_RECURSOS.TIPO_FUENTE_POAI IN ('1')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES
          ELSE 0
        END) LIBREDESTINACION_OBL,
        
        SUM(
        CASE
          WHEN FUENTE_RECURSOS.TIPO_FUENTE_POAI IN ('2')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES
          ELSE 0
        END) DESTINACIONESPECIFICA_OBL,
        SUM(
        CASE
          WHEN FUENTE_RECURSOS.TIPO_FUENTE_POAI IN ('3')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES
          ELSE 0
        END) RENTASCEDIDAS_OBL,   
        SUM(
        CASE
          WHEN FUENTE_RECURSOS.TIPO_FUENTE_POAI IN ('4')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES
          ELSE 0
        END) SGR_OBL,
        SUM(
        CASE
          WHEN FUENTE_RECURSOS.TIPO_FUENTE_POAI IN ('5')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES
          ELSE 0
        END) COMPENSACION_OBL,
       SUM(
        CASE
          WHEN FUENTE_RECURSOS.TIPO_FUENTE_POAI IN ('6')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES
          ELSE 0
        END) DESARROLLO_OBL,    
        SUM(
        CASE
          WHEN FUENTE_RECURSOS.TIPO_FUENTE_POAI IN ('7')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES
          ELSE 0
        END) CTEL_OBL,
        SUM(
        CASE
          WHEN FUENTE_RECURSOS.TIPO_FUENTE_POAI IN ('8')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES
          ELSE 0
        END) OTROS_OBL,
        SUM(
        CASE
          WHEN FUENTE_RECURSOS.TIPO_FUENTE_POAI IN ('9')
          THEN BP_PLAN_INDICATIVO_FUENTES.VALOR_OBLIGACIONES
          ELSE 0
        END) CREDITOS_OBL,
        BP_PLAN_INDICATIVO.DESCRIPCION,
        BP_PLAN_INDICATIVO.DEPENDENCIA
        
FROM PI_INDICADOR
  INNER JOIN PI_INDICADOR_META
    ON PI_INDICADOR.COMPANIA          = PI_INDICADOR_META.COMPANIA
    AND PI_INDICADOR.VIGENCIA_INICIAL = PI_INDICADOR_META.VIGENCIA_INICIAL
    AND PI_INDICADOR.ID_PLAN          = PI_INDICADOR_META.ID_PLAN
    AND PI_INDICADOR.CODIGO           = PI_INDICADOR_META.INDICADOR
  INNER JOIN BP_PLAN_INDICATIVO
    ON PI_INDICADOR_META.COMPANIA = BP_PLAN_INDICATIVO.COMPANIA
    AND PI_INDICADOR_META.ID_PLAN = BP_PLAN_INDICATIVO.ID
    AND PI_INDICADOR_META.VIGENCIA_INICIAL = BP_PLAN_INDICATIVO.VIGENCIA_INICIAL
  INNER JOIN BP_NIVEL_PLAN_IND
    ON BP_PLAN_INDICATIVO.COMPANIA          = BP_NIVEL_PLAN_IND.COMPANIA
    AND BP_PLAN_INDICATIVO.VIGENCIA_INICIAL = BP_NIVEL_PLAN_IND.VIGENCIA    
  LEFT JOIN  PI_RELACION_PROD_RTDO
    ON PI_INDICADOR.COMPANIA            = PI_RELACION_PROD_RTDO.COMPANIA
    AND PI_INDICADOR.VIGENCIA_INICIAL   = PI_RELACION_PROD_RTDO.VIGENCIA
    AND PI_INDICADOR.ID_PLAN            = PI_RELACION_PROD_RTDO.ID_PLAN_PRODUCTO
    AND PI_INDICADOR.CODIGO             = PI_RELACION_PROD_RTDO.CODIGO_PRODUCTO  
  LEFT JOIN SECTORES
    ON PI_INDICADOR.COMPANIA = SECTORES.COMPANIA
    AND PI_INDICADOR.SECTOR  = SECTORES.CODIGO 
  LEFT JOIN BP_PLAN_INDICATIVO_FUENTES
    ON BP_PLAN_INDICATIVO.COMPANIA          = BP_PLAN_INDICATIVO_FUENTES.COMPANIA
    AND BP_PLAN_INDICATIVO.ID               = BP_PLAN_INDICATIVO_FUENTES.ID_PLAN
    AND BP_PLAN_INDICATIVO.VIGENCIA_INICIAL = BP_PLAN_INDICATIVO_FUENTES.VIGENCIA_PLAN
    AND PI_INDICADOR_META.VIGENCIA_META = BP_PLAN_INDICATIVO_FUENTES.VIGENCIA_META
  LEFT JOIN FUENTE_RECURSOS
    ON BP_PLAN_INDICATIVO_FUENTES.COMPANIA = FUENTE_RECURSOS.COMPANIA
    AND BP_PLAN_INDICATIVO_FUENTES.VIGENCIA_META = FUENTE_RECURSOS.ANO
    AND BP_PLAN_INDICATIVO_FUENTES.FUENTE = FUENTE_RECURSOS.CODIGO
  LEFT JOIN FUENTE_EQUIVALENTE_DNP  
    ON FUENTE_RECURSOS.ANO          = FUENTE_EQUIVALENTE_DNP.ANO
    AND FUENTE_RECURSOS.CODIGO_DNP  = FUENTE_EQUIVALENTE_DNP.CODIGO 
  LEFT JOIN TIPO_FUENTE_POAI  
    ON FUENTE_RECURSOS.TIPO_FUENTE_POAI  = TIPO_FUENTE_POAI.CODIGO 
WHERE PI_INDICADOR_META.VIGENCIA_META BETWEEN TO_NUMBER(PCK_SYSMAN_UTL.FC_PAR(PI_INDICADOR.COMPANIA,'VIGENCIA GUBERNAMENTAL ACTUAL',52,SYSDATE,-1)) AND TO_NUMBER(PCK_SYSMAN_UTL.FC_PAR(PI_INDICADOR.COMPANIA,'VIGENCIA GUBERNAMENTAL ACTUAL',52,SYSDATE,-1)) + 3
    AND BP_NIVEL_PLAN_IND.META_PRODUC NOT IN(0)
    AND   LENGTH(PI_INDICADOR.ID_PLAN) = BP_NIVEL_PLAN_IND.DIGITOS
    GROUP BY         PI_INDICADOR.COMPANIA,
        PI_INDICADOR.VIGENCIA_INICIAL,
        PI_INDICADOR.ID_PLAN,
        PI_INDICADOR.CODIGO ,
        PI_INDICADOR_META.VIGENCIA_META,
        NVL(PCK_SYSMAN_UTL.FC_PAR(PI_INDICADOR.COMPANIA,'CODIGO DEPARTAMENTO SIEE',52,SYSDATE,-1),' '),
        NVL(PCK_SYSMAN_UTL.FC_PAR(PI_INDICADOR.COMPANIA,'NOMBRE DEPARTAMENTO SIEE',52,SYSDATE,-1),' ') ,
        NVL(PCK_SYSMAN_UTL.FC_PAR(PI_INDICADOR.COMPANIA,'CODIGO MUNICIPIO SIEE',52,SYSDATE,-1),' ') ,
        NVL(PCK_SYSMAN_UTL.FC_PAR(PI_INDICADOR.COMPANIA,'NOMBRE MUNICIPIO SIEE',52,SYSDATE,-1),' ') ,
        CASE WHEN PI_RELACION_PROD_RTDO.PRINCIPAL NOT IN(0) 
             THEN NVL(PI_INDICADOR.DESCRIPCION_META,' ')
          ELSE ' ' 
        END  ,
        CASE WHEN PI_RELACION_PROD_RTDO.PRINCIPAL NOT IN(0)  
          THEN PI_INDICADOR.NOMBRE 
          ELSE ' '
        END  ,
        CASE WHEN PI_RELACION_PROD_RTDO.PRINCIPAL NOT IN(0)  
          THEN PI_INDICADOR.LB
        END  ,
        CASE WHEN PI_RELACION_PROD_RTDO.PRINCIPAL NOT IN(0)   
          THEN PI_INDICADOR.META_CUATRENIO
        END ,
        NVL(PI_INDICADOR.DESCRIPCION_META,' ') ,
        PI_INDICADOR.NOMBRE ,
        PI_INDICADOR.LB ,
        PI_INDICADOR.META_CUATRENIO ,
        CASE PI_INDICADOR.TIPO_META_INDICADOR
          WHEN 'MI' THEN 'Incremento'
          WHEN 'MIA' THEN 'Incremento'
          WHEN 'MR' THEN 'Reducción'
          WHEN 'MRA' THEN 'Reducción'
          WHEN 'MM' THEN 'Mantenimiento'
        ELSE 
        ' '
        END ,
        PI_INDICADOR.SECTOR ,
        PI_INDICADOR.SECTOR || ' ' || SECTORES.DESCRIPCION ,
        PI_INDICADOR.ODS ,
        PI_INDICADOR.PILAR_MARCO,
        PI_INDICADOR_META.META,
        PI_INDICADOR_META.AVANCE,
        CASE WHEN PI_INDICADOR_META.META  NOT IN (0) THEN (PI_INDICADOR_META.AVANCE / PI_INDICADOR_META.META) * 100 ELSE 0  END,
        BP_PLAN_INDICATIVO.DESCRIPCION,
        BP_PLAN_INDICATIVO.DEPENDENCIA;