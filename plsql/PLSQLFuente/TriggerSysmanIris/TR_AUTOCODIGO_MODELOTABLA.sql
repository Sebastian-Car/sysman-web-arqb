create or replace TRIGGER "TR_AUTOCODIGO_MODELOTABLA" 
BEFORE  INSERT OR UPDATE ON MODELO_TABLA
FOR EACH ROW
BEGIN 
IF INSERTING THEN
SELECT SECUENCIA_MODELOTABLA.NEXTVAL INTO :NEW.CODIGO FROM DUAL;
END IF;
UPDATE MODELO_VARIABLES SET DESCRIPCION = :NEW.NOMBRE WHERE PLANTILLA =:OLD.PLANTILLA AND DESCRIPCION = :OLD.NOMBRE AND TIPO = 'T';
END;