                          Se cambió el trigger a compuesto y se utilizo un type para almacenar registros para cuando se hacen transacciones multiples
                          
  */
FOR INSERT OR DELETE OR UPDATE OF COMPANIA, TIPOCOBRO, CODIGO_COBRO, ANO, VALOR_NETO ON SF_DETALLE_COBRO 
   
   COMPOUND TRIGGER
   
TYPE REGISTRO IS RECORD
(
    COMPANIA            PCK_SUBTIPOS.TI_COMPANIA,
    TIPOCOBRO           VARCHAR2(10 CHAR),
    CODIGO_COBRO        PCK_SUBTIPOS.TI_ENTERO_LARGO,
    ANIO                PCK_SUBTIPOS.TI_ANIO
);
 
TYPE REGISTROS IS TABLE OF REGISTRO INDEX BY BINARY_INTEGER ;
  TABLAAUX REGISTROS;
  MI_POSACT NUMBER DEFAULT 0; 
 
AFTER EACH ROW IS
BEGIN
  
  MI_POSACT := MI_POSACT+1;
  
  IF INSERTING THEN 
      TABLAAUX(MI_POSACT).COMPANIA      := :NEW.COMPANIA;
      TABLAAUX(MI_POSACT).TIPOCOBRO     := :NEW.TIPOCOBRO;
      TABLAAUX(MI_POSACT).CODIGO_COBRO  := :NEW.CODIGO_COBRO;
      TABLAAUX(MI_POSACT).ANIO          := :NEW.ANO;
  ELSIF UPDATING THEN
      TABLAAUX(MI_POSACT).COMPANIA      := :NEW.COMPANIA;
      TABLAAUX(MI_POSACT).TIPOCOBRO     := :NEW.TIPOCOBRO;
      TABLAAUX(MI_POSACT).CODIGO_COBRO  := :NEW.CODIGO_COBRO;
      TABLAAUX(MI_POSACT).ANIO          := :NEW.ANO;
      
      IF :OLD.COMPANIA <>:NEW.COMPANIA OR :OLD.TIPOCOBRO <>:NEW.TIPOCOBRO 
      OR :OLD.CODIGO_COBRO<>:NEW.CODIGO_COBRO OR :OLD.ANO<>:NEW.ANO THEN
        MI_POSACT := MI_POSACT+1;
        TABLAAUX(MI_POSACT).COMPANIA      := :OLD.COMPANIA;
        TABLAAUX(MI_POSACT).TIPOCOBRO     := :OLD.TIPOCOBRO;
        TABLAAUX(MI_POSACT).CODIGO_COBRO  := :OLD.CODIGO_COBRO;
        TABLAAUX(MI_POSACT).ANIO          := :OLD.ANO;  
      END IF;
  ELSIF DELETING THEN 
      TABLAAUX(MI_POSACT).COMPANIA      := :OLD.COMPANIA;
      TABLAAUX(MI_POSACT).TIPOCOBRO     := :OLD.TIPOCOBRO;
      TABLAAUX(MI_POSACT).CODIGO_COBRO  := :OLD.CODIGO_COBRO;
      TABLAAUX(MI_POSACT).ANIO          := :OLD.ANO;
  END IF;
END AFTER EACH ROW;
  --
  AFTER STATEMENT IS 
    BEGIN 
     FOR i IN 1..MI_POSACT LOOP
      PCK_FACT_GENERAL_COM2.PR_ACTUALIZAR_VALOR_TOTAL( UN_COMPANIA      => TABLAAUX(MI_POSACT).COMPANIA,
                                                       UN_TIPOCOBRO     => TABLAAUX(MI_POSACT).TIPOCOBRO,
                                                       UN_CODIGO_COBRO  => TABLAAUX(MI_POSACT).CODIGO_COBRO,
                                                       UN_ANIO          => TABLAAUX(MI_POSACT).ANIO) ;
    
    END LOOP;                                                   
  END AFTER STATEMENT;
  END;
