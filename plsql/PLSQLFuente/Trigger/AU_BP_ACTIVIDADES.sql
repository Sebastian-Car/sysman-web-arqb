AFTER UPDATE OF NOMBRE ON BP_ACTIVIDADES
FOR EACH ROW
BEGIN
  UPDATE COMPONENTES_ACTIVIDADES 
  SET COMPONENTES_ACTIVIDADES.NOMBREACTIVIDAD = :NEW.NOMBRE
  WHERE COMPONENTES_ACTIVIDADES.ACTIVIDAD = :NEW.CODIGO;
END;