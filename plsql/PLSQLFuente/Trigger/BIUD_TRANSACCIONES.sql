      DESCRIPTION       : Se ajusta el proceso para que no permite cambiar la transacción si ya esta validada en contabilidad               
*/ 
BEFORE DELETE OR INSERT OR UPDATE ON TRANSACCIONES 
FOR EACH ROW 
DECLARE
    MI_REEMPLAZOS PCK_SUBTIPOS.TI_CLAVEVALOR;
BEGIN
    IF :OLD.TIPO_CPTE IS NOT NULL OR :OLD.COMPROBANTE IS NOT NULL THEN
        RAISE PCK_EXCEPCIONES.EXC_TRANSAUTOMAT;    
    END IF;
EXCEPTION WHEN PCK_EXCEPCIONES.EXC_TRANSAUTOMAT THEN  
    MI_REEMPLAZOS(0).CLAVE := 'TIPO';
    MI_REEMPLAZOS(0).VALOR := :NEW.TIPO;
    MI_REEMPLAZOS(1).CLAVE := 'MODELO';
    MI_REEMPLAZOS(1).VALOR := :NEW.NUMERO_MODELO;
    MI_REEMPLAZOS(2).CLAVE := 'NUMERO';
    MI_REEMPLAZOS(2).VALOR := :NEW.NUMERO;
    MI_REEMPLAZOS(3).CLAVE := 'TIPO_COM';
    MI_REEMPLAZOS(3).VALOR := :NEW.TIPO_CPTE;
    MI_REEMPLAZOS(4).CLAVE := 'COMPROBANE';
    MI_REEMPLAZOS(4).VALOR := :NEW.COMPROBANTE;
    PCK_ERR_MSG.RAISE_WITH_MSG(
                 UN_EXC_COD    => SQLCODE,
                 UN_TABLAERROR => 'TRANSACCIONES',
                 UN_ERROR_COD  => PCK_ERRORES.ERR_SYSMANTA_TRANSACCIONCONCOM,
                 UN_REEMPLAZOS => MI_REEMPLAZOS
               );
END;