                          se desea insetar, actualizar o borrar.
  */
FOR INSERT OR UPDATE OR DELETE
ON  BP_NIVEL_PLAN_IND
COMPOUND TRIGGER
TYPE REGISTRO IS RECORD
(
  COMPANIA               PCK_SUBTIPOS.TI_COMPANIA,
  VIGENCIA               PCK_SUBTIPOS.TI_ANIO,
  DIGITOS                BP_NIVEL_PLAN_IND.DIGITOS%TYPE,
  AUXRESUL               PCK_SUBTIPOS.TI_LOGICO,
  AUXPRODUCT             PCK_SUBTIPOS.TI_LOGICO,
  AUXRESULANT            PCK_SUBTIPOS.TI_LOGICO,
  AUXPRODUCTANT          PCK_SUBTIPOS.TI_LOGICO,
  DIGITOSANT             BP_NIVEL_PLAN_IND.DIGITOS%TYPE,
  MAN_TRAZADOR           PCK_SUBTIPOS.TI_LOGICO,
  MAN_TRAZADOR_ANT       PCK_SUBTIPOS.TI_LOGICO,
  OPCION                 VARCHAR2(10 CHAR)
);
TYPE REGISTROS IS TABLE OF REGISTRO INDEX BY BINARY_INTEGER ;
  TABLAAUX REGISTROS;
  MI_POSACT NUMBER DEFAULT 0;
AFTER EACH ROW IS
BEGIN
  MI_POSACT := MI_POSACT+1;
  TABLAAUX(MI_POSACT).COMPANIA      := :NEW.COMPANIA;
  TABLAAUX(MI_POSACT).VIGENCIA      := :NEW.VIGENCIA;
  TABLAAUX(MI_POSACT).DIGITOS       := :NEW.DIGITOS;
  TABLAAUX(MI_POSACT).AUXRESUL      := :NEW.META_RESUL;
  TABLAAUX(MI_POSACT).AUXPRODUCT    := :NEW.META_PRODUC;
  TABLAAUX(MI_POSACT).MAN_TRAZADOR   := :NEW.MANEJA_TRAZADOR;
  TABLAAUX(MI_POSACT).AUXRESULANT   := :OLD.META_RESUL;
  TABLAAUX(MI_POSACT).AUXPRODUCTANT := :OLD.META_PRODUC;
  TABLAAUX(MI_POSACT).MAN_TRAZADOR_ANT   := :OLD.MANEJA_TRAZADOR;
  TABLAAUX(MI_POSACT).DIGITOSANT    := :OLD.DIGITOS;
  --tu campo 
  IF DELETING
  THEN
    TABLAAUX(MI_POSACT).COMPANIA := :OLD.COMPANIA;
    TABLAAUX(MI_POSACT).VIGENCIA := :OLD.VIGENCIA;
    TABLAAUX(MI_POSACT).DIGITOS  := :OLD.DIGITOS;
    TABLAAUX(MI_POSACT).OPCION   := 'BORRAR';
  ELSE
    TABLAAUX(MI_POSACT).OPCION       := NULL;
  END IF;
END AFTER EACH ROW;
AFTER STATEMENT IS
BEGIN
  FOR i IN 1..MI_POSACT LOOP
    PCK_BANCOS_PROY4.PR_REVISAR_IND_PLANINDICATIVO(UN_COMPANIA      => TABLAAUX(MI_POSACT).COMPANIA,
                                                   UN_VIGENCIA      => TABLAAUX(MI_POSACT).VIGENCIA,
                                                   UN_DIGITOS       => TABLAAUX(MI_POSACT).DIGITOS,
                                                   UN_AUXRESUL      => TABLAAUX(MI_POSACT).AUXRESUL,
                                                   UN_AUXPRODUCT    => TABLAAUX(MI_POSACT).AUXPRODUCT,
                                                   UN_MAN_TRAZADOR  => TABLAAUX(MI_POSACT).MAN_TRAZADOR,
                                                   UN_AUXRESULANT   => TABLAAUX(MI_POSACT).AUXRESULANT,
                                                   UN_AUXPRODUCTANT => TABLAAUX(MI_POSACT).AUXPRODUCTANT,
                                                   UN_MAN_TRAZADOR_ANT => TABLAAUX(MI_POSACT).MAN_TRAZADOR_ANT,
                                                   UN_DIGITOSANT    => TABLAAUX(MI_POSACT).DIGITOSANT,
                                                   UN_OPCION        => TABLAAUX(MI_POSACT).OPCION);
  END LOOP;
  END AFTER STATEMENT;
END;