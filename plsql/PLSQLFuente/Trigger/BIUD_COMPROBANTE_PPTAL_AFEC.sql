CREATE OR REPLACE TRIGGER "BIUD_COMPROBANTE_PPTAL_AFEC"  
  /*
    NAME              : BIUD_COMPROBANTE_PPTAL_AFEC
    AUTHORS           : SYSMAN SAS
    AUTHOR MIGRACION  : 
    DATE MIGRADOR     : 
    TIME              : 
    DESCRIPTION       : 
    MODIFIER          : JUAN CARLOS RODRÍGUEZ AMÉZQUITA
    DATE MODIFIED     : 08/09/2017
    TIME              : 03:30 PM
    MODIFICATIONS     : Validación de cambio de fecha en el comprobante.
  */ 
BEFORE DELETE OR UPDATE OF COMPANIA, ANO, TIPO, NUMERO, AUXILIAR, CENTRO_COSTO, 
                           CREDITO, DEBITO, FECHA, FUENTE_RECURSO, MES, 
                           REFERENCIA, SUCURSAL, TERCERO
ON COMPROBANTE_PPTAL 
FOR EACH ROW
DECLARE
  MI_RTA PCK_SUBTIPOS.TI_LOGICO DEFAULT 0;
  MI_ACCION  VARCHAR2(100);
BEGIN
  IF UPDATING THEN
    MI_ACCION:='actualizar';
  ELSIF INSERTING THEN
    MI_ACCION:='insertar';
  ELSE 
    MI_ACCION:='borrar';
  END IF;
  IF TO_CHAR(:OLD.FECHA, 'DD/MM/YYYY HH24:MI:SS') <> 
     TO_CHAR(:NEW.FECHA, 'DD/MM/YYYY HH24:MI:SS') THEN
    MI_RTA    := PCK_PRESUPUESTO.FC_VALIDA_AFECTACIONES_PPTAL(
      UN_COMPANIA => :OLD.COMPANIA, 
      UN_ANO_AFECT => :OLD.ANO, 
      UN_TIPO_CPTE_AFECT => :OLD.TIPO, 
      UN_COMPROBANTE_AFECT => :OLD.NUMERO);
    IF MI_RTA <>0 THEN
      DECLARE
        MI_MSGERROR PCK_SUBTIPOS.TI_CLAVEVALOR;
      BEGIN
        RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;
      EXCEPTION
      WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN
        MI_MSGERROR(1).CLAVE := 'ANO';
        MI_MSGERROR(1).VALOR := :OLD.ANO;
        MI_MSGERROR(2).CLAVE := 'TIPO';
        MI_MSGERROR(2).VALOR := :OLD.TIPO;
        MI_MSGERROR(3).CLAVE := 'COMPROBANTE';
        MI_MSGERROR(3).VALOR := :OLD.NUMERO;
        MI_MSGERROR(4).CLAVE := 'ACCION';
        MI_MSGERROR(4).VALOR := MI_ACCION;
        PCK_ERR_MSG.RAISE_WITH_MSG( 
          UN_EXC_COD => SQLCODE, 
          UN_TABLAERROR=>'COMPROBANTE_PPTAL', 
          UN_ERROR_COD => PCK_ERRORES.ERR_PRESU_COMPROBANTE_AFECT, 
          UN_REEMPLAZOS => MI_MSGERROR );
      END;
    END IF;
  END IF;
END;
