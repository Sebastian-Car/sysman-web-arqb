CREATE OR REPLACE TRIGGER "BIU_COMPROBANTE_CNTRETENCION"  
BEFORE INSERT OR UPDATE ON COMPROBANTE_CNTRETENCION
FOR EACH ROW
/*
NAME             : BIU_COMPROBANTE_CNTRETENCION
AUTHORS          : SYSMAN SAS
AUTHOR MIGRACION : JUAN CARLOS RODRÍGUEZ AMÉZQUITA
DATE MIGRADOR    : 18/04/2017
TIME             : 03:15 PM
MODIFIER         :
DATE MODIFIED    :
TIME MODIFIED    :
DESCRIPTION      : Trigger antes de insertar y actualizar para la tabla 
                   COMPROBANTE_CNTRETENCION, para validar el valor de la 
                   retención y el valor de la base gravable. 
                   Basado en el comportamiento del formulario Comprobante_cntRetencion 
                   de la versión de MS Access SysmanCT2016.04.03
*/
DECLARE
  MI_REEMPLAZOS                               PCK_SUBTIPOS.TI_CLAVEVALOR;
  MI_PERMITE_MODIFICAR                        PCK_SUBTIPOS.TI_LOGICO;
BEGIN
  -- Validación del valor de la retención
  BEGIN
    MI_PERMITE_MODIFICAR := PCK_CONTABILIDAD5.FC_PERMITE_CAMBIAR_VALOR (
      UN_COMPANIA => :NEW.COMPANIA
    , UN_TIPO     => :NEW.TIPORETENCION
    , UN_ANO      => :NEW.ANO
    , UN_CODIGO   => :NEW.CODIGORETENCION);
    IF MI_PERMITE_MODIFICAR = 0 THEN
      IF INSERTING AND :NEW.VALOR <> 0 AND :NEW.CALCULADO=0 THEN
        RAISE PCK_EXCEPCIONES.EXC_CONTABILIDAD;
      END IF;
      IF UPDATING AND :NEW.VALOR <> :OLD.VALOR AND :OLD.CALCULADO <> 0 THEN
        RAISE PCK_EXCEPCIONES.EXC_CONTABILIDAD;
      END IF;
    END IF;
  EXCEPTION 
  WHEN PCK_EXCEPCIONES.EXC_CONTABILIDAD THEN
    MI_REEMPLAZOS(1).CLAVE := 'TIPORETENCION';
    MI_REEMPLAZOS(1).VALOR := :NEW.TIPORETENCION;
    MI_REEMPLAZOS(2).CLAVE := 'CODIGORETENCION';
    MI_REEMPLAZOS(2).VALOR := :NEW.CODIGORETENCION;
    PCK_ERR_MSG.RAISE_WITH_MSG(
      UN_EXC_COD    => SQLCODE
    , UN_TABLAERROR => 'COMPROBANTE_CNTRETENCION'
    , UN_ERROR_COD  => PCK_ERRORES.ERR_CONTAB_CAMBIAR_VALORRETEN
    , UN_REEMPLAZOS => MI_REEMPLAZOS
    );
  END;
  /*Valida que el valor de la base gravable no sea inferior que el límite 
  inferior de retenciones*/
  DECLARE
    MI_ES_VALOR_VALIDO                        PCK_SUBTIPOS.TI_LOGICO;
  BEGIN
    MI_ES_VALOR_VALIDO := PCK_CONTABILIDAD5.FC_LIMITE_INFERIOR_RETEN (
      UN_COMPANIA  => :NEW.COMPANIA
    , UN_TIPO      => :NEW.TIPORETENCION
    , UN_ANO       => :NEW.ANO
    , UN_CODIGO    => :NEW.CODIGORETENCION
    , UN_VALORBASE => :NEW.VALORBASE);
    IF MI_ES_VALOR_VALIDO = 0 THEN
      RAISE PCK_EXCEPCIONES.EXC_CONTABILIDAD;
    END IF;
  EXCEPTION 
  WHEN PCK_EXCEPCIONES.EXC_CONTABILIDAD THEN
   PCK_ERR_MSG.RAISE_WITH_MSG(
      UN_EXC_COD    => SQLCODE
    , UN_TABLAERROR => 'COMPROBANTE_CNTRETENCION'
    , UN_ERROR_COD  => PCK_ERRORES.ERR_CONTAB_BASE_INFERIOR_LIMIT
    );
  END;
  -- Validación del valor de la base gravable
  DECLARE
    MI_VLR_BASE_COMPROBANTE                  PCK_SUBTIPOS.TI_DOBLE;
  BEGIN
    MI_PERMITE_MODIFICAR := PCK_CONTABILIDAD5.FC_PERMITE_CAMBIAR_VALORBASE (
      UN_COMPANIA => :NEW.COMPANIA
    , UN_TIPO     => :NEW.TIPORETENCION
    , UN_ANO      => :NEW.ANO
    , UN_CODIGO   => :NEW.CODIGORETENCION);  
    IF MI_PERMITE_MODIFICAR = 0  THEN
      IF INSERTING THEN
        SELECT CASE WHEN :NEW.TIPORETENCION='IVA' AND 
                  PCK_SYSMAN_UTL.FC_PAR(UN_COMPANIA   => :NEW.COMPANIA,
                                        UN_NOMBRE     => 'MONTO SOMETIDO RETENCION IVA = VALOR IVA', 
                                        UN_MODULO     => 1, 
                                        UN_FECHA_PAR  => SYSDATE) ='SI'
              THEN  VLR_BASEIVA ELSE VLR_BASE END
         INTO MI_VLR_BASE_COMPROBANTE
         FROM COMPROBANTE_CNT
        WHERE COMPANIA = :NEW.COMPANIA
          AND ANO      = :NEW.ANO
          AND TIPO     = :NEW.TIPO
          AND NUMERO   = :NEW.NUMERO;
        IF :NEW.VALORBASE <> MI_VLR_BASE_COMPROBANTE THEN
          RAISE PCK_EXCEPCIONES.EXC_CONTABILIDAD;
        END IF;
      END IF;
      IF UPDATING AND :NEW.VALORBASE <> :OLD.VALORBASE THEN
        RAISE PCK_EXCEPCIONES.EXC_CONTABILIDAD;
      END IF;
    END IF;
  EXCEPTION
  WHEN PCK_EXCEPCIONES.EXC_CONTABILIDAD THEN
    MI_REEMPLAZOS(1).CLAVE := 'TIPORETENCION';
    MI_REEMPLAZOS(1).VALOR := :NEW.TIPORETENCION;
    MI_REEMPLAZOS(2).CLAVE := 'CODIGORETENCION';
    MI_REEMPLAZOS(2).VALOR := :NEW.CODIGORETENCION;
    PCK_ERR_MSG.RAISE_WITH_MSG(
      UN_EXC_COD    => SQLCODE
    , UN_TABLAERROR => 'COMPROBANTE_CNTRETENCION'
    , UN_ERROR_COD  => PCK_ERRORES.ERR_CONTAB_CAMBIAR_VALORBASE
    , UN_REEMPLAZOS => MI_REEMPLAZOS
    );
  END;
END BIU_COMPROBANTE_CNTRETENCION;
