CREATE OR REPLACE TRIGGER BI_FORMULAS_PROCEDIMIENTO 
/*
    NAME              : BI_FORMULAS_PROCEDIMIENTO
    AUTHORS           : SYSMAN SAS
    AUTHOR MIGRACION  :
    DATE MIGRADOR     :
    TIME              :
    SOURCE MODULE     :
    MODIFIER          : 
    DATE MODIFIED     : 
    TIME              : 
    DESCRIPTION       : TRIGGER PARA GENERACION AUTOMATICA DEL ID
                        EN LA TABLA FORMULAS_PROCEDIMIENTO
*/
BEFORE INSERT ON FORMULAS_PROCEDIMIENTO FOR EACH ROW
BEGIN
    IF :NEW.ID IS NULL THEN
        SELECT NVL(MAX(ID), 0) + 1
        INTO :NEW.ID
        FROM FORMULAS_PROCEDIMIENTO;
    END IF;
END;
