CREATE OR REPLACE TRIGGER "CU_COMPROBANTE_CNT_ACT_DET"  
  /*
    NAME              : CU_COMPROBANTE_CNT_ACT_DET
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : José Pascual Gómez
    DATE MIGRADOR     : 08/06/2017
    TIME              : 17:10 
    MODIFIER          :
    DATE MODIFIED     :
    TIME              :
    DESCRIPTION       : Trigger se crea como compuesto debido a que al actualizar el detalle
                        se revisa la fecha del header y genera mutación de tabla    
    */
FOR UPDATE OF  
                             COMPANIA
                           , ANO
                           , TIPO
                           , NUMERO
                           , FECHA
                           , TERCERO
                           , SUCURSAL
                           , DESCRIPCION 
                           , NRO_DOCUMENTO
                           , REFERENCIA
                           , AUXILIAR
ON COMPROBANTE_CNT
COMPOUND TRIGGER 
MI_RTA                 PCK_SUBTIPOS.TI_RTA_ACME;
  TYPE REGISTRO IS RECORD 
  (
  COMPANIA            PCK_SUBTIPOS.TI_COMPANIA,
  ANO                 PCK_SUBTIPOS.TI_ANIO,
  TIPO                COMPROBANTE_CNT.TIPO%TYPE,
  COMPROBANTE         COMPROBANTE_CNT.NUMERO%TYPE,
  FECHAA              COMPROBANTE_CNT.FECHA%TYPE,
  TERCEROA            COMPROBANTE_CNT.TERCERO%TYPE,
  SUCURSALA           COMPROBANTE_CNT.SUCURSAL%TYPE,
  DESCRIPCIONA        COMPROBANTE_CNT.DESCRIPCION%TYPE,
  NUMERODOCA          COMPROBANTE_CNT.NRO_DOCUMENTO%TYPE,
  REFERENCIAA         COMPROBANTE_CNT.REFERENCIA%TYPE,
  AUXILIARA           PCK_SUBTIPOS.TI_AUXILIAR,
  FECHAN              COMPROBANTE_CNT.FECHA%TYPE,
  TERCERON            COMPROBANTE_CNT.TERCERO%TYPE,
  SUCURSALN           COMPROBANTE_CNT.SUCURSAL%TYPE,
  DESCRIPCIONN        COMPROBANTE_CNT.DESCRIPCION%TYPE,
  NUMERODOCN          COMPROBANTE_CNT.NRO_DOCUMENTO%TYPE,
  REFERENCIAN         COMPROBANTE_CNT.REFERENCIA%TYPE,
  AUXILIARN           PCK_SUBTIPOS.TI_AUXILIAR
	);  
  TYPE REGISTROS IS TABLE OF REGISTRO INDEX BY BINARY_INTEGER ;
  TABLACT REGISTROS;
  MI_POSACT NUMBER DEFAULT 0;
  RTA    NUMBER;
  ACTUALIZA  NUMBER;
AFTER EACH ROW IS
BEGIN
  
       MI_POSACT := MI_POSACT+1;
       TABLACT(MI_POSACT).COMPANIA            := :NEW.COMPANIA;
       TABLACT(MI_POSACT).ANO                 := :NEW.ANO;
       TABLACT(MI_POSACT).TIPO                := :NEW.TIPO;
       TABLACT(MI_POSACT).COMPROBANTE         := :NEW.NUMERO;
       TABLACT(MI_POSACT).FECHAA              := :OLD.FECHA;
       TABLACT(MI_POSACT).TERCEROA            := :OLD.TERCERO;
       TABLACT(MI_POSACT).SUCURSALA           := :OLD.SUCURSAL;
       TABLACT(MI_POSACT).DESCRIPCIONA        := :OLD.DESCRIPCION;
       TABLACT(MI_POSACT).NUMERODOCA          := :OLD.NRO_DOCUMENTO;
       TABLACT(MI_POSACT).REFERENCIAA         := :OLD.REFERENCIA;
       TABLACT(MI_POSACT).AUXILIARA           := :OLD.AUXILIAR;
       TABLACT(MI_POSACT).FECHAN              := :NEW.FECHA;
       TABLACT(MI_POSACT).TERCERON            := :NEW.TERCERO;
       TABLACT(MI_POSACT).SUCURSALN           := :NEW.SUCURSAL;
       TABLACT(MI_POSACT).DESCRIPCIONN        := :NEW.DESCRIPCION;
       TABLACT(MI_POSACT).NUMERODOCN          := :NEW.NRO_DOCUMENTO;
       TABLACT(MI_POSACT).REFERENCIAN         := :NEW.REFERENCIA;
       TABLACT(MI_POSACT).AUXILIARN           := :NEW.AUXILIAR;
   
END AFTER EACH ROW;
AFTER STATEMENT IS
BEGIN
  FOR i IN 1..MI_POSACT LOOP
	 MI_RTA := PCK_CONTABILIDAD2.FC_ACTUALIZARDETALLECNT(
			   UN_COMPANIA     => TABLACT(MI_POSACT).COMPANIA, 
			   UN_ANO          => TABLACT(MI_POSACT).ANO, 
			   UN_TIPO         => TABLACT(MI_POSACT).TIPO, 
			   UN_COMPROBANTE  => TABLACT(MI_POSACT).COMPROBANTE,
			   UN_FECHAA       => 'TO_DATE('''||TO_CHAR(TABLACT(MI_POSACT).FECHAA, 'DD/MM/YYYY HH:mi:ss')|| ''', ''DD/MM/YYYY HH24:MI:SS'')', 
			   UN_TERCEROA     => TABLACT(MI_POSACT).TERCEROA, 
			   UN_SUCURSALA    => TABLACT(MI_POSACT).SUCURSALA, 
			   UN_DESCRIPCIONA => TABLACT(MI_POSACT).DESCRIPCIONA, 
			   UN_NUMERODOCA   => TABLACT(MI_POSACT).NUMERODOCA, 
			   UN_REFERENCIAA  => TABLACT(MI_POSACT).REFERENCIAA, 
			   UN_AUXILIARA    => TABLACT(MI_POSACT).AUXILIARA,
			   UN_FECHAN       => 'TO_DATE('''||TO_CHAR(TABLACT(MI_POSACT).FECHAN, 'DD/MM/YYYY HH:mi:ss')|| ''', ''DD/MM/YYYY HH24:MI:SS'')', 
			   UN_TERCERON     => TABLACT(MI_POSACT).TERCERON, 
			   UN_SUCURSALN    => TABLACT(MI_POSACT).SUCURSALN, 
			   UN_DESCRIPCIONN => TABLACT(MI_POSACT).DESCRIPCIONN , 
			   UN_NUMERODOCN   => TABLACT(MI_POSACT).NUMERODOCN , 
			   UN_REFERENCIAN  => TABLACT(MI_POSACT).REFERENCIAN, 
			   UN_AUXILIARN    => TABLACT(MI_POSACT).AUXILIARN);   
  END LOOP;                                   
  END AFTER STATEMENT;
END;