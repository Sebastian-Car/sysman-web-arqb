      DESCRIPTION       : CONTROLA NO BORRAR CUENTAS MAYORES CON HIJOS O QUE TENGAN SALDO 
                          
  */

FOR DELETE ON PLAN_CONTABLE
REFERENCING OLD AS OLD NEW AS NEW 
COMPOUND TRIGGER
    TYPE REGISTRO IS RECORD 
    (
        MI_COMPANIA        PCK_SUBTIPOS.TI_COMPANIA,
        MI_ANO             PCK_SUBTIPOS.TI_ANIO,
        MI_CODIGO          PCK_SUBTIPOS.TI_CODIGOCONTA
    );  
    TYPE REGISTROS IS TABLE OF REGISTRO INDEX BY BINARY_INTEGER ;
    TABLA        REGISTROS;
    MI_POS       NUMBER DEFAULT 0;

BEFORE EACH ROW IS
BEGIN

    IF :OLD.SALDO0 +:OLD.SALDO1 + :OLD.SALDO2 +:OLD.SALDO3 +:OLD.SALDO4 +:OLD.SALDO5 
       +:OLD.SALDO6 +:OLD.SALDO7 +:OLD.SALDO8 +:OLD.SALDO9 +:OLD.SALDO10 
       +:OLD.SALDO11 +:OLD.SALDO12+ :OLD.SALDO13 <>0 THEN  
      DECLARE
        MI_MSGERROR        PCK_SUBTIPOS.TI_CLAVEVALOR;  
      BEGIN
          RAISE PCK_EXCEPCIONES.EXC_CONTABILIDAD;        
      EXCEPTION WHEN PCK_EXCEPCIONES.EXC_CONTABILIDAD THEN 
        MI_MSGERROR(1).CLAVE := 'CODIGO';
        MI_MSGERROR(1).VALOR := :OLD.CODIGO;
        MI_MSGERROR(2).CLAVE := 'ANO';
        MI_MSGERROR(2).VALOR := :OLD.ANO;
         PCK_ERR_MSG.RAISE_WITH_MSG(
                UN_EXC_COD    => SQLCODE
               ,UN_TABLAERROR => 'PLAN_CONTABLE'
               ,UN_ERROR_COD  => PCK_ERRORES.ERR_CONTAB_ELIMINACUENTASALDO
               ,UN_REEMPLAZOS => MI_MSGERROR
               ); 
      END;   
    END IF;
      
    
    MI_POS:=MI_POS+1;
    TABLA(MI_POS).MI_COMPANIA   :=:OLD.COMPANIA;
    TABLA(MI_POS).MI_ANO        :=:OLD.ANO;
    TABLA(MI_POS).MI_CODIGO     :=:OLD.CODIGO; 
                         
END BEFORE EACH ROW;
 
AFTER STATEMENT IS                 
BEGIN 
    
  FOR MI_I IN 1..MI_POS
    LOOP
       PCK_CONTABILIDAD_CPTE.PR_ELIMINAR_CUENTA(
                          UN_COMPANIA   => TABLA(MI_I).MI_COMPANIA
                         ,UN_ANO        => TABLA(MI_I).MI_ANO
                         ,UN_CODIGO     => TABLA(MI_I).MI_CODIGO); 
    END LOOP;

END AFTER STATEMENT;
END;
