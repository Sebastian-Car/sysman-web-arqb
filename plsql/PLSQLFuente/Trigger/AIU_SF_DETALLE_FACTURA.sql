      TIME              : 
      SOURCE MODULE     : 
      MODIFIER          : 
      DATE MODIFIED     : 
      TIME              : 
      DESCRIPTION       : ACTUALIZA EL VALOR TOTAL DE LA FACTURA DEPENDIEND DEL OBJETO DE COBRO
                          
  */

FOR INSERT OR UPDATE  ON SF_DETAllE_FACTURA
   
   COMPOUND TRIGGER
   
TYPE REGISTRO IS RECORD
(
    COMPANIA            PCK_SUBTIPOS.TI_COMPANIA,
    TIPO_FACTURA        SF_DETAllE_FACTURA.TIPO_FACTURA%TYPE,    
    NUMERO_FACTURA      SF_DETAllE_FACTURA.FACTURA%TYPE,
    ANIO                SF_DETAllE_FACTURA.ANO%TYPE
);
 
TYPE REGISTROS IS TABLE OF REGISTRO INDEX BY BINARY_INTEGER ;
  TABLAAUX REGISTROS;
  MI_POSACT NUMBER DEFAULT 0; 
 
AFTER EACH ROW IS

BEGIN
  
  
  
  
  MI_POSACT := MI_POSACT+1;
  
  IF INSERTING THEN 
      TABLAAUX(MI_POSACT).COMPANIA         := :NEW.COMPANIA;
      TABLAAUX(MI_POSACT).TIPO_FACTURA     := :NEW.TIPO_FACTURA;
      TABLAAUX(MI_POSACT).NUMERO_FACTURA   := :NEW.FACTURA;      
      TABLAAUX(MI_POSACT).ANIO             := :NEW.ANO;
  ELSIF UPDATING THEN
      TABLAAUX(MI_POSACT).COMPANIA        := :NEW.COMPANIA;
      TABLAAUX(MI_POSACT).TIPO_FACTURA    := :NEW.TIPO_FACTURA;
      TABLAAUX(MI_POSACT).NUMERO_FACTURA  := :NEW.FACTURA;      
      TABLAAUX(MI_POSACT).ANIO            := :NEW.ANO;

      IF :OLD.COMPANIA <>:NEW.COMPANIA OR :OLD.TIPO_FACTURA <>:NEW.TIPO_FACTURA 
      OR :OLD.FACTURA<>:NEW.FACTURA       
      OR :OLD.ANO<>:NEW.ANO  THEN
        MI_POSACT := MI_POSACT+1;
        TABLAAUX(MI_POSACT).COMPANIA        := :OLD.COMPANIA;
        TABLAAUX(MI_POSACT).TIPO_FACTURA    := :OLD.TIPO_FACTURA;
        TABLAAUX(MI_POSACT).NUMERO_FACTURA  := :OLD.FACTURA;        
        TABLAAUX(MI_POSACT).ANIO            := :OLD.ANO;  
      END IF;

  END IF;
END AFTER EACH ROW;
  --
  AFTER STATEMENT IS 
    BEGIN 
     FOR i IN 1..MI_POSACT LOOP
      PCK_FACT_GENERAL_COM3.PR_ACT_VALOR_TOTAL_FACTURA( UN_COMPANIA      => TABLAAUX(MI_POSACT).COMPANIA,
                                                       UN_TIPOCOBRO      => TABLAAUX(MI_POSACT).TIPO_FACTURA,
                                                       UN_NUMERO_FACTURA => TABLAAUX(MI_POSACT).NUMERO_FACTURA,
                                                       UN_ANIO           => TABLAAUX(MI_POSACT).ANIO) ;
    
    END LOOP;                                                   
  END AFTER STATEMENT;
  END;
