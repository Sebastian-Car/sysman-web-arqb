CREATE OR REPLACE TRIGGER "BIU_DETALLE_COMPROBANTE_PPTAL"  
/*
  NAME              : BIU_DETALLE_COMPROBANTE_PPTAL
  AUTHORS           : SYSMAN  SAS
  AUTHOR MIGRACION  : 
  DATE MIGRADOR     : 
  TIME              : 
  SOURCE MODULE     : 
  MODIFIER          : JAVIER ANDRES RODRIGUEZ RIOS
  DATE MODIFIED     : 31/01/2017
  TIME              : 09:30 AM
  DESCRIPTION       : SE AJUSTA AL ESTANDAR           
  MODIFIER          : Carlos Alberto Manrique Palacios
  DATE MODIFIED     : 06/02/2017
  TIME              : 05:11 PM
  DESCRIPTION       : Se modifica este trigger al eliminar las columnas vituales de la base de datos            
*/ 
BEFORE
INSERT OR
UPDATE OF COMPANIA ,
  ANO ,
  CUENTA ,
  VALOR_DEBITO ,
  VALOR_CREDITO ,
  AUXILIAR,
  TERCERO,
  REFERENCIA,
  CENTRO_COSTO,
  FUENTE_RECURSO,
  SUCURSAL,
  FECHA,
  AUXILIARI,
  TERCEROI,
  REFERENCIAI,
  CENTRO_COSTOI,
  DIA,
  FUENTE_RECURSOI,
  SUCURSALI,
  MES,
  ID
ON DETALLE_COMPROBANTE_PPTAL 
REFERENCING OLD AS ANT NEW AS NUE 
FOR EACH ROW 
DECLARE  
  MI_X            NUMBER          := 0;
  MI_NATURALEZA   VARCHAR2(1 CHAR);
  MI_RTA          VARCHAR2(32000);
  MI_MSGERROR     PCK_SUBTIPOS.TI_CLAVEVALOR;
  
BEGIN
    IF PCK_GENERALES.FC_CONS_CAMBIONIT() IN (0) THEN --VALIDACION PARA CUANDO SE ESTA CAMBIANDO EL NIT AL TERCERO
      BEGIN 
        BEGIN
          SELECT  MAN_CEN_CTO 
                  + MAN_AUX_TER 
                  + MAN_AUX_GEN 
                  + MAN_AUX_FUE 
                  + MAN_AUX_REF 
                  + MOVIMIENTO
                 ,NATURALEZA  
            INTO  MI_X
                 ,MI_NATURALEZA
            FROM PLAN_PRESUPUESTAL  
           WHERE COMPANIA = :NUE.COMPANIA
             AND ANO      = :NUE.ANO
             AND CODIGO   = :NUE.CUENTA; 
        EXCEPTION WHEN NO_DATA_FOUND THEN 
          RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;
        END;
      EXCEPTION WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN 
        MI_MSGERROR(1).CLAVE := 'CUENTA';
        MI_MSGERROR(1).VALOR := :NUE.CUENTA;
        MI_MSGERROR(2).CLAVE := 'ANIO';
        MI_MSGERROR(2).VALOR := :NUE.ANO;
        MI_MSGERROR(3).CLAVE := 'COMPANIA';
        MI_MSGERROR(3).VALOR := :NUE.COMPANIA;
        PCK_ERR_MSG.RAISE_WITH_MSG(
                    UN_EXC_COD    => SQLCODE
                   ,UN_TABLAERROR => 'DETALLE_COMPROBANTE_PPTAL'
                   ,UN_ERROR_COD  => PCK_ERRORES.ERR_CONTAB_NO_EXISTECUENTA
                   ,UN_REEMPLAZOS => MI_MSGERROR
        );
      END;
      BEGIN
        IF MI_X=0 THEN
          RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO; 
        END IF;
      EXCEPTION WHEN  PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN 
        MI_MSGERROR(1).CLAVE := 'CUENTA';
        MI_MSGERROR(1).VALOR := :NUE.CUENTA;
        PCK_ERR_MSG.RAISE_WITH_MSG(
                    UN_EXC_COD    => SQLCODE
                   ,UN_ERROR_COD  => PCK_ERRORES.ERRR_PRESUPUESTO_SIN_IND
                   ,UN_TABLAERROR => 'DETALLE_COMPROBANTE_PPTAL'
                   ,UN_REEMPLAZOS => MI_MSGERROR
         );
      END;
      :NUE.NATURALEZA:= MI_NATURALEZA;
      :NUE.ID:=PCK_SYSMAN_UTL.FC_CODIGO_PPTAL(:NUE.COMPANIA,:NUE.ANO,:NUE.CUENTA,:NUE.CENTRO_COSTO,:NUE.TERCERO,:NUE.SUCURSAL,:NUE.AUXILIAR,:NUE.REFERENCIA,:NUE.FUENTE_RECURSO);
        
      IF INSERTING THEN
        :NUE.AUXILIARI      :=PCK_SYSMAN_UTL.FC_GEN_AUXILIARPPTAL_VIRTUAL(:NUE.COMPANIA,:NUE.ANO,:NUE.CUENTA,:NUE.AUXILIAR,4);
        :NUE.TERCEROI       :=PCK_SYSMAN_UTL.FC_GEN_AUXILIARPPTAL_VIRTUAL(:NUE.COMPANIA,:NUE.ANO,:NUE.CUENTA,:NUE.TERCERO,2);
        :NUE.REFERENCIAI    :=PCK_SYSMAN_UTL.FC_GEN_AUXILIARPPTAL_VIRTUAL(:NUE.COMPANIA,:NUE.ANO,:NUE.CUENTA,:NUE.REFERENCIA,5);
        :NUE.CENTRO_COSTOI  :=PCK_SYSMAN_UTL.FC_GEN_AUXILIARPPTAL_VIRTUAL(:NUE.COMPANIA,:NUE.ANO,:NUE.CUENTA,:NUE.CENTRO_COSTO,1);
        :NUE.FUENTE_RECURSOI:=PCK_SYSMAN_UTL.FC_GEN_AUXILIARPPTAL_VIRTUAL(:NUE.COMPANIA,:NUE.ANO,:NUE.CUENTA,:NUE.FUENTE_RECURSO,6);
        :NUE.SUCURSALI      :=PCK_SYSMAN_UTL.FC_GEN_AUXILIARPPTAL_VIRTUAL(:NUE.COMPANIA,:NUE.ANO,:NUE.CUENTA,:NUE.SUCURSAL,3);
        :NUE.DIA            :=TO_NUMBER(TO_CHAR(:NUE.FECHA,'DD'));
        :NUE.MES            :=TO_NUMBER(TO_CHAR(:NUE.FECHA,'MM'));
      ELSIF UPDATING THEN 
        IF UPDATING('COMPANIA') OR UPDATING('ANO') OR UPDATING('CUENTA') OR UPDATING('AUXILIAR')THEN
          :NUE.AUXILIARI:=PCK_SYSMAN_UTL.FC_GEN_AUXILIARPPTAL_VIRTUAL(:NUE.COMPANIA,:NUE.ANO,:NUE.CUENTA,:NUE.AUXILIAR,4);
        END IF;
        IF UPDATING('COMPANIA') OR UPDATING('ANO') OR UPDATING('CUENTA') OR UPDATING('TERCERO') OR  UPDATING('SUCURSAL') THEN
          :NUE.TERCEROI:=PCK_SYSMAN_UTL.FC_GEN_AUXILIARPPTAL_VIRTUAL(:NUE.COMPANIA,:NUE.ANO,:NUE.CUENTA,:NUE.TERCERO,2);
          :NUE.SUCURSALI:=PCK_SYSMAN_UTL.FC_GEN_AUXILIARPPTAL_VIRTUAL(:NUE.COMPANIA,:NUE.ANO,:NUE.CUENTA,:NUE.SUCURSAL,3);
        END IF;
        IF UPDATING('COMPANIA') OR UPDATING('ANO') OR UPDATING('CUENTA') OR UPDATING('REFERENCIA')THEN
          :NUE.REFERENCIAI:=PCK_SYSMAN_UTL.FC_GEN_AUXILIARPPTAL_VIRTUAL(:NUE.COMPANIA,:NUE.ANO,:NUE.CUENTA,:NUE.REFERENCIA,5);
        END IF;
        IF UPDATING('COMPANIA') OR UPDATING('ANO') OR UPDATING('CUENTA') OR UPDATING('CENTRO_COSTO')THEN
          :NUE.CENTRO_COSTOI:=PCK_SYSMAN_UTL.FC_GEN_AUXILIARPPTAL_VIRTUAL(:NUE.COMPANIA,:NUE.ANO,:NUE.CUENTA,:NUE.CENTRO_COSTO,1);
        END IF;
        IF UPDATING('COMPANIA') OR UPDATING('ANO') OR UPDATING('CUENTA') OR UPDATING('FUENTE_RECURSO')THEN
          :NUE.FUENTE_RECURSOI:=PCK_SYSMAN_UTL.FC_GEN_AUXILIARPPTAL_VIRTUAL(:NUE.COMPANIA,:NUE.ANO,:NUE.CUENTA,:NUE.FUENTE_RECURSO,6);
        END IF;
      END IF;
      IF UPDATING('FECHA')THEN
        :NUE.DIA:=TO_NUMBER(TO_CHAR(:NUE.FECHA,'DD'));
        :NUE.MES:=TO_NUMBER(TO_CHAR(:NUE.FECHA,'MM'));
      END IF;  
    END IF;
END;