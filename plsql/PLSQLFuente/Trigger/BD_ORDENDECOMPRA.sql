      DATE MODIFIED     : 25/04/2017
      TIME              : 10:35 AM
      DESCRIPTION       : SE ENVIA EL CODIGO AL PROCEDIMIENTO PR_ELIMINARORDENDECOMPRA
                          --se desactiva el trigger no funcionan las validaciones al verificar las afectaciones por adiciones    
                          
  */
FOR DELETE ON ORDENDECOMPRA
REFERENCING OLD AS OLD NEW AS NEW 
COMPOUND TRIGGER
    TYPE REGISTRO IS RECORD 
    (
        MI_COMPANIA        PCK_SUBTIPOS.TI_COMPANIA,
        MI_CLASEORDEN      ORDENDECOMPRA.CLASEORDEN%TYPE,
        MI_NUMERO          ORDENDECOMPRA.NUMERO%TYPE
    );  
    TYPE REGISTROS IS TABLE OF REGISTRO INDEX BY BINARY_INTEGER ;
    TABLA        REGISTROS;
    MI_POS       NUMBER DEFAULT 0;
    MI_EXISTE    PCK_SUBTIPOS.TI_LOGICO;
    MI_TABLA     PCK_SUBTIPOS.TI_TABLA; 
    MI_CONDICION PCK_SUBTIPOS.TI_CONDICION;
BEFORE EACH ROW IS
BEGIN
    MI_POS:=MI_POS+1;
    TABLA(MI_POS).MI_COMPANIA   :=:OLD.COMPANIA;
    TABLA(MI_POS).MI_CLASEORDEN :=:OLD.CLASEORDEN;
    TABLA(MI_POS).MI_NUMERO     :=:OLD.NUMERO;
END BEFORE EACH ROW;
BEFORE STATEMENT IS
    BEGIN 
        FOR MI_I IN 1..MI_POS 
        LOOP
          MI_EXISTE:= PCK_CONTRATOS_COM1.FC_ELIMINARORDENDECOMPRA(
                              UN_COMPANIA   => TABLA(MI_I).MI_COMPANIA
                             ,UN_CLASEORDEN => TABLA(MI_I).MI_CLASEORDEN
                             ,UN_NUMERO     => TABLA(MI_I).MI_NUMERO);        
        END LOOP;   
    END BEFORE STATEMENT;
END;
