      DATE MODIFIED     : 
      TIME              : 
      DESCRIPTION       : REALIZA LA ACTUALIZACIÓN DEL VALOR TOTAL DE UNA SOLICITUD (SOLICITUDDISPONIBILIDAD) TENIENDO ENCUENTA LA SUMATORIA DE SUS DETALLES (D_SOLICITUDDISPONIBILIDAD)
                          
  */
FOR INSERT OR DELETE OR UPDATE OF VALOR ON D_SOLICITUDDISPONIBILIDAD 
    REFERENCING OLD AS OLD NEW AS NEW
    COMPOUND TRIGGER
    MI_COMPANIA        PCK_SUBTIPOS.TI_COMPANIA;
    MI_SOLICITUD       SOLICITUDDISPONIBILIDAD.NUMERO%TYPE;
    MI_TIPO_SOLICITUD  SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD%TYPE;

    AFTER EACH ROW IS
    BEGIN
      IF INSERTING THEN 
          MI_COMPANIA       := :NEW.COMPANIA;
          MI_SOLICITUD      := :NEW.NUMERO;
          MI_TIPO_SOLICITUD := :NEW.TIPO_SOLICITUD;
      ELSIF UPDATING THEN 
          MI_COMPANIA       := :OLD.COMPANIA;
          MI_SOLICITUD      := :OLD.NUMERO;
          MI_TIPO_SOLICITUD := :OLD.TIPO_SOLICITUD;
      ELSIF DELETING THEN 
          MI_COMPANIA       := :OLD.COMPANIA;
          MI_SOLICITUD      := :OLD.NUMERO;
          MI_TIPO_SOLICITUD := :OLD.TIPO_SOLICITUD;
      END IF;
    END AFTER EACH ROW;
    
        AFTER STATEMENT IS 
      BEGIN 
        PCK_PRESUPUESTO3.PR_ACTUALIZAR_VALOR_SOLICITUD( UN_COMPANIA      => MI_COMPANIA,
                                                        UN_SOLICITUD     => MI_SOLICITUD,
                                                        UN_TIPO_SOLICITUD=> MI_TIPO_SOLICITUD);
    END AFTER STATEMENT;
END;