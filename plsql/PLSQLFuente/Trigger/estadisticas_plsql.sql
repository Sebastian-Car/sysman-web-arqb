BEGIN
FOR RS IN (SELECT TABLE_NAME FROM USER_TABLES) LOOP
DBMS_STATS.GATHER_TABLE_STATS ( OWNNAME=>USER 
                                  , TABNAME=>RS.TABLE_NAME);
END LOOP;
END;
/


BEGIN
    FOR rs IN (SELECT INDEX_NAME FROM USER_INDEXES) LOOP
        DBMS_STATS.GATHER_INDEX_STATS(
            OWNNAME   => USER,
            INDNAME   => rs.INDEX_NAME
        );
    END LOOP;
END;
/

COMMIT;