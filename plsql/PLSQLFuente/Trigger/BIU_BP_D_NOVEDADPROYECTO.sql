CREATE OR REPLACE TRIGGER BIU_BP_D_NOVEDADPROYECTO 
 /*
        NAME              : BIU_BP_D_NOVEDADPROYECTO
        AUTHORS           : GERMAN DAVID ROJAS
        DATE MIGRADOR     : 24/11/2022
        TIME              : 
        MODIFIER          :
        DATE MODIFIED     :
        TIME              :
        DESCRIPTION       : Trigger que actualiza el valor disminuido en la solicitud de banco de banco de proyectos
                            de acuerdo a los disminuido en el certificado.
*/

FOR UPDATE OF VALORDISMINUIDO ON BP_D_NOVEDADPROYECTO 
COMPOUND TRIGGER

  MI_DOCUMENTO_AFECTAR          NUMBER(20,0);
  MI_DEPENDENCIA_AFECTAR        VARCHAR2(12 CHAR);
  MI_TIPOT_AFECTAR              VARCHAR2(3 CHAR);
  MI_COMPANIA                   VARCHAR2(11 CHAR);
  MI_VALORDISMINUIDO            NUMBER;
  MI_CODIGO                     NUMBER;
  MI_VALORSOLICITADO            NUMBER;
  MI_VALORAPROBADO              NUMBER;
  BEFORE EACH ROW IS
  BEGIN
    SELECT
    DOCUMENTO_AFECTAR, 
    DEPENDENCIA_AFECTAR, 
    TIPOT_AFECTAR
    INTO MI_DOCUMENTO_AFECTAR,
    MI_DEPENDENCIA_AFECTAR,
    MI_TIPOT_AFECTAR
    FROM BPNOVEDADPROYECTO
    WHERE 
    BPNOVEDADPROYECTO.COMPANIA = :NEW.COMPANIA
    AND BPNOVEDADPROYECTO.TIPOT = :NEW.TIPOT 
    AND BPNOVEDADPROYECTO.CLASET = :NEW.CLASET 
    AND BPNOVEDADPROYECTO.CODIGO = :NEW.NOVEDAD
    AND BPNOVEDADPROYECTO.DEPENDENCIA = :NEW.DEPENDENCIA;
    
    MI_CODIGO := :NEW.CODIGO;
    MI_VALORDISMINUIDO := :NEW.VALORDISMINUIDO;
    MI_COMPANIA := :NEW.COMPANIA;
    MI_VALORSOLICITADO := :OLD.VALORSOLICITADO;
    MI_VALORAPROBADO:= (MI_VALORSOLICITADO-MI_VALORDISMINUIDO);
  END BEFORE EACH ROW;

  AFTER STATEMENT IS
  BEGIN
   IF(MI_DOCUMENTO_AFECTAR <> 0) THEN 
   UPDATE BP_D_NOVEDADPROYECTO SET VALORDISMINUIDO = MI_VALORDISMINUIDO, VALORAPROBADO = MI_VALORAPROBADO
    WHERE 
    NOVEDAD = MI_DOCUMENTO_AFECTAR        
    AND DEPENDENCIA = MI_DEPENDENCIA_AFECTAR           
    AND TIPOT = MI_TIPOT_AFECTAR    
    AND COMPANIA = MI_COMPANIA
    AND CODIGO = MI_CODIGO;
  END IF;
   
  END AFTER STATEMENT ;

END;