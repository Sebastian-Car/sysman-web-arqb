FOR EACH ROW
BEGIN
  BEGIN
    IF :OLD.CODIGO=PCK_DATOS.CONS_FUENTE THEN
      RAISE PCK_EXCEPCIONES.EXC_GENERAL;
    END IF;
  EXCEPTION WHEN PCK_EXCEPCIONES.EXC_GENERAL THEN 
