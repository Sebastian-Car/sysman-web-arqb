CREATE OR REPLACE TRIGGER BU_BASESNOVEDADES 
  /*
      NAME              : BU_BASESNOVEDADES
      AUTHORS           : STEFANINI SYSMAN
      AUTHOR MIGRACION  : ANDREA CAROLINA PINEDA
      DATE MIGRADOR     : 05/10/2018
      TIME              : 11:24 AM
      SOURCE MODULE     : 
      MODIFIER          : 
      DATE MODIFIED     : 
      TIME              : 
      DESCRIPTION       : ACTUALIZA EL INDICADOR MANUAL EN CASO DE QUE EL USUARIO REALICE CAMBIOS SOBRE LOS CAMPOS BASE, DIAS, FECHAINICIAL Y FECHAFINAL
  */
BEFORE UPDATE OF BASE,DIAS,FECHAINICIAL,FECHAFINAL, MANUAL ON BASESNOVEDADES 
REFERENCING  OLD AS OLD NEW AS NEW
FOR EACH ROW
BEGIN
  IF (:NEW.BASE <> :OLD.BASE) OR (:NEW.DIAS <> :OLD.DIAS) OR (:NEW.FECHAINICIAL <> :OLD.FECHAINICIAL) OR (:NEW.FECHAFINAL <> :OLD.FECHAFINAL) THEN
    :NEW.MANUAL := -1;    
    :NEW.FECHACAMBIOMANUAL := SYSDATE;
  END IF;
END;