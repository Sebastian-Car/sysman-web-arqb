CREATE OR REPLACE TRIGGER "AU_MOVIMIENTO" 
  /*
  NAME              : AU_MOVIMIENTO
  AUTHORS           : SYSMAN  SAS
  AUTHOR MIGRACION  :
  DATE MIGRADOR     :
  TIME              :
  SOURCE MODULE     :
  MODIFIER          : JAVIER ANDRES RODRIGUEZ RIOS
  DATE MODIFIED     : 31/01/2017
  TIME              : 08:00 AM
  DESCRIPTION       : Se ajusta al estándar
  */
  FOR UPDATE OF AJUSTE_DEC_MANUAL ON MOVIMIENTO 
  COMPOUND TRIGGER 
  MI_COMPANIA                      VARCHAR2(32000) ;
  MI_TIPOMOVIMIENTO                VARCHAR2(32000) ;
  MI_MOVIMIENTO                    NUMBER ;
  MI_RTA                           VARCHAR2(32000) ;
  MI_VALORES                       PCK_SUBTIPOS.TI_VALORES;
  MI_TABLA                         PCK_SUBTIPOS.TI_TABLA;
  MI_PARAMETROS                    PCK_SUBTIPOS.TI_CAMPOS;
  MI_CAMPOS                        PCK_SUBTIPOS.TI_CAMPOS;
  MI_CONDICION                     PCK_SUBTIPOS.TI_CAMPOS;
  MI_PORCENTAJE                    PCK_SUBTIPOS.TI_DOBLE;
  MI_PARAMETRO                     VARCHAR2(255 CHAR);
AFTER EACH ROW IS
BEGIN
  IF UPDATING THEN
    MI_COMPANIA       :=:NEW.COMPANIA;
    MI_TIPOMOVIMIENTO :=:NEW.TIPOMOVIMIENTO;
    MI_MOVIMIENTO     :=:NEW.NUMERO;
  END IF;
END AFTER EACH ROW;
AFTER STATEMENT IS
BEGIN
  IF UPDATING THEN
    MI_RTA := PCK_ALMACEN.FC_ACTUALIZAVALORTOTAL( 
      UN_COMPANIA => MI_COMPANIA
    , UN_TIPOMOVIMIENTO => MI_TIPOMOVIMIENTO
    , UN_MOVIMIENTO => MI_MOVIMIENTO
    , UN_PORCIVA => 0
    , UN_VALORUNITARIO => 0
    , UN_CANTIDAD_NUEVA => 0
    , UN_CODIGO => 0);
    
    MI_PARAMETRO := NVL(PCK_SYSMAN_UTL.FC_PAR(MI_COMPANIA,'APLICAR AIU EN ENTRADAS DE ALMACEN',PCK_DATOS.MODULOALMACEN,SYSDATE),'NO');
    IF MI_PARAMETRO = 'SI' THEN 
        BEGIN 
            SELECT SUM(ADMINISTRACION + IMPREVISTOS + UTILIDADES)
               INTO MI_PORCENTAJE
            FROM MOVIMIENTO
            WHERE COMPANIA = MI_COMPANIA
            AND TIPOMOVIMIENTO = MI_TIPOMOVIMIENTO
            AND NUMERO = MI_MOVIMIENTO;
        EXCEPTION WHEN NO_DATA_FOUND THEN
            MI_PORCENTAJE := 0;
        END;
        BEGIN
          MI_TABLA:='D_MOVIMIENTO';
          MI_VALORES:=' PORC_AIU  = ''' || MI_PORCENTAJE || ''' ';
          MI_CONDICION:=' COMPANIA       = ''' || MI_COMPANIA || ''''||
                        ' AND TIPOMOVIMIENTO  = ''' || MI_TIPOMOVIMIENTO || ''''||
                        ' AND MOVIMIENTO  = ''' || MI_MOVIMIENTO || '''';
          BEGIN
            PCK_DATOS.GL_RTA := PCK_DATOS.FC_ACME (UN_TABLA        => MI_TABLA,
                                            UN_ACCION       => 'M',
                                            UN_CAMPOS       => MI_VALORES,
                                            UN_CONDICION    => MI_CONDICION);
              EXCEPTION
                   WHEN  PCK_EXCEPCIONES.EXC_ACTUALIZAR THEN
                  RAISE  PCK_EXCEPCIONES.EXC_ALMACEN;
          END;
               EXCEPTION WHEN PCK_EXCEPCIONES.EXC_ALMACEN THEN
                PCK_ERR_MSG.RAISE_WITH_MSG(
                  UN_EXC_COD    => SQLCODE
                  ,UN_ERROR_COD  => PCK_ERRORES.ERR_ALMACEN_DELETE_DEPRECIAR);
      END;
           FOR RS IN (SELECT COMPANIA, TIPOMOVIMIENTO, MOVIMIENTO, CODIGO, CANTIDAD, PORCIVA, VALORUNITARIO FROM D_MOVIMIENTO
                WHERE COMPANIA = MI_COMPANIA
                AND TIPOMOVIMIENTO = MI_TIPOMOVIMIENTO
                AND MOVIMIENTO = MI_MOVIMIENTO)
                LOOP
             MI_RTA := PCK_ALMACEN.FC_ACTUALIZAVALORTOTAL(
          UN_COMPANIA => MI_COMPANIA
        , UN_TIPOMOVIMIENTO => MI_TIPOMOVIMIENTO
        , UN_MOVIMIENTO => MI_MOVIMIENTO
        , UN_PORCIVA => RS.PORCIVA
        , UN_VALORUNITARIO => RS.VALORUNITARIO
        , UN_CANTIDAD_NUEVA => RS.CANTIDAD
        , UN_CODIGO => RS.CODIGO);   
         END LOOP;
      END IF;
  END IF;
END AFTER STATEMENT;
END;
