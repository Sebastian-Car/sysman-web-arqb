      DATE MODIFIED     : 30/08/2017
      TIME              : 08:55 AM
      DESCRIPTION       : CONTROLA NO BORRAR RUBROS MAYORES CON HIJOS O QUE TENGAN SALDO 
                          
  */

FOR DELETE ON PLAN_PRESUPUESTAL
REFERENCING OLD AS OLD NEW AS NEW 
COMPOUND TRIGGER
    TYPE REGISTRO IS RECORD 
    (
        MI_COMPANIA        PCK_SUBTIPOS.TI_COMPANIA,
        MI_ANO             PCK_SUBTIPOS.TI_ANIO,
        MI_CODIGO          PCK_SUBTIPOS.TI_CODIGOPPTAL
    );  
    TYPE REGISTROS IS TABLE OF REGISTRO INDEX BY BINARY_INTEGER ;
    TABLA        REGISTROS;
    MI_POS       NUMBER DEFAULT 0;
    MI_SALDO     PCK_SUBTIPOS.TI_DOBLE;

BEFORE EACH ROW IS
BEGIN
  BEGIN
    SELECT SUM(NVL(ABS(ADICION),0)                     +NVL(ABS(REDUCCION),0)          +NVL(ABS(PAC_APROPIADO),0)            +NVL(ABS(PAC_PROGRAMADO),0)
          +NVL(ABS(DISPONIBILIDAD),0)              +NVL(ABS(DISPONIBILIDADADD),0)  +NVL(ABS(DISPONIBILIDADDMD),0)        +NVL(ABS(REG_NO_CONTRACT),0)
          +NVL(ABS(REG_CONTRACT),0)                +NVL(ABS(REG_REVERSION),0)      +NVL(ABS(MODIF_PAC_DEBITO),0)         +NVL(ABS(MODIF_PAC_CREDITO),0)
          +NVL(ABS(MODIF_REG_CONT),0)              +NVL(ABS(MODIF_REG_NOCONT),0)   +NVL(ABS(MODIF_REG_CONTADR),0)        +NVL(ABS(MODIF_REG_NOCONTADR),0)
          +NVL(ABS(MODIF_REG_CONTDMR),0)           +NVL(ABS(MODIF_REG_NOCONTDMR),0)+NVL(ABS(REINTEGRO),0)                +NVL(ABS(VIGENCIAANTERIOR),0)
          +NVL(ABS(VIGENCIAFUTURA),0)              +NVL(ABS(TRASLADO_DEBITO),0)    +NVL(ABS(TRASLADO_CREDITO),0)         +NVL(ABS(APLAZAM_DEBITO),0)
          +NVL(ABS(APLAZAM_CREDITO),0)             +NVL(ABS(EJE_CNT_DEBITO),0)     +NVL(ABS(EJE_CNT_CREDITO),0)          +NVL(ABS(APROPIACION_DEBITO),0)
          +NVL(ABS(APROPIACION_CREDITO),0)         +NVL(ABS(EJE_PPT_DEBITO),0)     +NVL(ABS(EJE_PPT_CREDITO),0)          +NVL(ABS(EJE_PPT_DEBITOAEG),0)
          +NVL(ABS(EJE_PPT_DEBITODEG),0)           +NVL(ABS(REGISTRO_OBLIGACION),0)+NVL(ABS(MODIF_REGISTRO_OBLIGACION),0)+NVL(ABS(MODIF_REGISTRO_OBLIGACIONARO),0)
          +NVL(ABS(MODIF_REGISTRO_OBLIGACIONDRO),0)+NVL(ABS(INGRESOS_EFECTIVO),0)  +NVL(ABS(INGRESOS_PAPELES),0)         +NVL(ABS(MODIF_INGRESOS_EFECTIVO),0)
          +NVL(ABS(MODIF_INGRESOS_PAPELES),0)      +NVL(ABS(INGRESOS_CAUSADOS),0)  +NVL(ABS(MODIF_INGRESOS_CAUSADOS),0)  +NVL(ABS(MODIF_INGRESOS),0)
          +NVL(ABS(NETOEGRESO),0)                  +NVL(ABS(RECONOCIMIENTOS),0)    +NVL(ABS(PACTESORERIA),0)             +NVL(ABS(PAC_EJECUTADO),0)+NVL(ABS(PAC_COMPROMETIDO),0)) 
          INTO MI_SALDO
    FROM SALDO_PLAN_PPTAL
    WHERE COMPANIA=:OLD.COMPANIA
    AND ANO=:OLD.ANO
    AND CODIGO=:OLD.CODIGO
    GROUP BY COMPANIA,ANO,CODIGO;
  EXCEPTION WHEN NO_DATA_FOUND THEN
    MI_SALDO:=0;
  END;
   IF MI_SALDO<>0 THEN
      DECLARE
        MI_MSGERROR        PCK_SUBTIPOS.TI_CLAVEVALOR;  
      BEGIN
          RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;        
      EXCEPTION WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN 
        MI_MSGERROR(1).CLAVE := 'CODIGO';
        MI_MSGERROR(1).VALOR := :OLD.CODIGO;
        MI_MSGERROR(2).CLAVE := 'ANO';
        MI_MSGERROR(2).VALOR := :OLD.ANO;
         PCK_ERR_MSG.RAISE_WITH_MSG(
                UN_EXC_COD    => SQLCODE
               ,UN_TABLAERROR => 'PLAN_PRESUPUESTAL'
               ,UN_ERROR_COD  => PCK_ERRORES.ERR_PRESU_ELIMINACUENTASALDO
               ,UN_REEMPLAZOS => MI_MSGERROR
               ); 
      END;   
   END IF;
    MI_POS:=MI_POS+1;
    TABLA(MI_POS).MI_COMPANIA   :=:OLD.COMPANIA;
    TABLA(MI_POS).MI_ANO        :=:OLD.ANO;
    TABLA(MI_POS).MI_CODIGO     :=:OLD.CODIGO; 
                         
END BEFORE EACH ROW;
 
AFTER STATEMENT IS                 
BEGIN 
  FOR MI_I IN 1..MI_POS
    LOOP
       PCK_PRESUPUESTO3.PR_ELIMINAR_CUENTA_PPTAL(
                          UN_COMPANIA   => TABLA(MI_I).MI_COMPANIA
                         ,UN_ANO        => TABLA(MI_I).MI_ANO
                         ,UN_CODIGO     => TABLA(MI_I).MI_CODIGO); 
    END LOOP;
END AFTER STATEMENT;
END;
