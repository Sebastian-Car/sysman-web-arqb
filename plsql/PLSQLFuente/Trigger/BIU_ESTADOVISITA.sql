CREATE OR REPLACE TRIGGER "BIU_ESTADOVISITA"  
/*
      NAME              : BIU_ESTADOVISITA
      AUTHORS           : SYSMAN  SAS
      AUTHOR MIGRACION  : 
      DATE MIGRADOR     : 
      TIME              : 
      SOURCE MODULE     : 
      MODIFIER          : JAVIER ANDRES RODRIGUEZ RIOS
      DATE MODIFIED     : 31/01/2017
      TIME              : 10:05 AM
      DESCRIPTION       : SE AJUSTA AL ESTANDAR                          
*/
BEFORE UPDATE OR INSERT ON ESTADO_VISITA 
FOR EACH ROW
DECLARE
  MI_CANT     NUMBER DEFAULT 0; -- ALMACENA LA CANTIDAD DE REGISTROS QUE CUMPLAN CON LA CONDICIÓN 
BEGIN
  BEGIN 
    IF :NEW.IND_REPROGRAMAR NOT IN (0) 
       AND :NEW.IND_CIERRE NOT IN (0) THEN
      RAISE PCK_EXCEPCIONES.EXC_PROGRAMACION_VISITAS;
      --RAISE_APPLICATION_ERROR(-20000,'Un estado no puede tener indicador de cierre y de reprogramado a la vez'||SQLERRM);
    END IF;
  EXCEPTION WHEN PCK_EXCEPCIONES.EXC_PROGRAMACION_VISITAS THEN
    PCK_ERR_MSG.RAISE_WITH_MSG(
                UN_EXC_COD    => SQLCODE
               ,UN_TABLAERROR => 'ESTADO_VISITA'
               ,UN_ERROR_COD  => PCK_ERRORES.ERR_PROGVISITAS_INDCIERREREPRO
               );
  END;
  IF :NEW.IND_REPROGRAMAR NOT IN (0) THEN
    BEGIN
      SELECT COUNT(0) CANT
        INTO MI_CANT 
        FROM ESTADO_VISITA 
       WHERE COMPANIA  = :NEW.COMPANIA
         AND IND_REPROGRAMAR NOT IN (0);
    EXCEPTION WHEN NO_DATA_FOUND THEN
      MI_CANT :=0;
    END ;
  END IF;
  BEGIN 
    IF MI_CANT NOT IN (0) THEN 
      RAISE PCK_EXCEPCIONES.EXC_PROGRAMACION_VISITAS;
      --RAISE_APPLICATION_ERROR(-20000,'Ya existe algún estado de visita con indicador de Reprogramado '||SQLERRM);
    END IF;
  EXCEPTION WHEN PCK_EXCEPCIONES.EXC_PROGRAMACION_VISITAS THEN
    PCK_ERR_MSG.RAISE_WITH_MSG(
                UN_EXC_COD    => SQLCODE
               ,UN_TABLAERROR => 'ESTADO_VISITA'
               ,UN_ERROR_COD  => PCK_ERRORES.ERR_PROGVISITAS_IND_REPROGRA
               );
  END;
  IF  :NEW.IND_CIERRE NOT IN (0) THEN
    BEGIN
      SELECT COUNT(0) CANT
        INTO MI_CANT  
        FROM ESTADO_VISITA 
       WHERE COMPANIA  = :NEW.COMPANIA
         AND IND_CIERRE NOT IN (0);
    EXCEPTION WHEN NO_DATA_FOUND THEN
      MI_CANT :=0;
    END ;
  END IF;
  BEGIN
    IF MI_CANT NOT IN (0) THEN 
      RAISE PCK_EXCEPCIONES.EXC_PROGRAMACION_VISITAS;
      --RAISE_APPLICATION_ERROR(-20000,'Ya existe algún estado de visita con indicador de Cierre '||SQLERRM);
    END IF;
  EXCEPTION WHEN PCK_EXCEPCIONES.EXC_PROGRAMACION_VISITAS THEN
    PCK_ERR_MSG.RAISE_WITH_MSG(
                UN_EXC_COD    => SQLCODE
               ,UN_TABLAERROR => 'ESTADO_VISITA'
               ,UN_ERROR_COD  => PCK_ERRORES.ERR_PROGVISITAS_IND_CIERRE
               );
  END;
END;
