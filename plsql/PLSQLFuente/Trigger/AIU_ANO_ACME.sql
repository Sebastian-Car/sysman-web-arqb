create or replace TRIGGER "AIU_ANO_ACME" FOR INSERT OR UPDATE ON ANO 
COMPOUND TRIGGER

  MI_COMPANIA VARCHAR2(32000);
  MI_NUMERO   NUMBER;
  MI_EXISTE   NUMBER:=0;
  MI_CAMPOS   VARCHAR2(32000);
  MI_VALORES  VARCHAR2(32000);
  MI_RTA      NUMBER;  
  MI_ANIO_ANT NUMBER DEFAULT 0;
  
  -- ES COMPUESTO POR QUE SE CONSULTA LA TABLA Y SE CREA UNA TABLA MUTANTE
  -- CON ESTE TIPO GUARDO LOS REGISTROS POR CADA REGISTRO DEL INSERT O UPDATE PARA LUEGO PROCESARLO
  TYPE REGISTRO IS RECORD 
  (
    COMPANIA                  ANO.COMPANIA%TYPE,
    NUMERO                    ANO.NUMERO%TYPE,
    CREATED_BY                ANO.CREATED_BY%TYPE
  );  
  TYPE REGISTROS IS TABLE OF REGISTRO INDEX BY BINARY_INTEGER ;
  TABLA REGISTROS;
  MI_POS NUMBER DEFAULT 0;
  RTA    NUMBER;
  
AFTER EACH ROW IS
BEGIN
  --POR CADA FILA SE CARGA LA TABLA PARA LUEGO LARZAR EL REGISTRO
  MI_POS := MI_POS+1;
  TABLA(MI_POS).COMPANIA       := :NEW.COMPANIA;
  TABLA(MI_POS).NUMERO         := :NEW.NUMERO;
  IF UPDATING THEN
    TABLA(MI_POS).CREATED_BY     := :NEW.MODIFIED_BY;  
  ELSE
    TABLA(MI_POS).CREATED_BY     := :NEW.CREATED_BY;  
  END IF;
END AFTER EACH ROW;

AFTER STATEMENT IS
BEGIN
  FOR i IN 1..MI_POS LOOP
    RTA:= PCK_SYSMAN_UTL.FC_INSERTARMESESDELANO(
            UN_COMPANIA => TABLA(MI_POS).COMPANIA,
            UN_ANIO     => TABLA(MI_POS).NUMERO,
            UN_CREATED_BY => TABLA(MI_POS).CREATED_BY);   
END LOOP;

-- AL MOMENTO DE INSERTAR UN NUEVO AÑO SE COPIAN LOS DATOS DEL AÑO ANTERIOR AL NUEVO QUE SE ESTÁ REGISTRANDO
-- SOLO SE PASAN LOS DATOS REFERENCIADOS EN LA PARTE INFERIOR 
 IF (INSERTING OR UPDATING) AND RTA IS NOT NULL THEN 
    MI_ANIO_ANT := NVL(TABLA(MI_POS).NUMERO, EXTRACT(YEAR FROM SYSDATE)) - 1;

    BEGIN
      SELECT NUMERO 
      INTO MI_ANIO_ANT
      FROM ANO
      WHERE COMPANIA = TABLA(MI_POS).COMPANIA
      AND NUMERO = MI_ANIO_ANT;

    EXCEPTION WHEN NO_DATA_FOUND THEN
      -- Si no se encuentra un año anterior, se permite continuar con el proceso
      MI_ANIO_ANT := NULL;  -- No hay año anterior
    END;

    -- Verificación de la condición de MI_ANIO_ANT para la tabla ANO
    IF MI_ANIO_ANT IS NOT NULL THEN
      -- Continuar con el proceso para la tabla ANO si existe un año anterior
      PCK_PREPARAR_ANO.PR_COPIAR_AUXILIAR(TABLA(MI_POS).COMPANIA, TABLA(MI_POS).NUMERO, MI_ANIO_ANT, TABLA(MI_POS).COMPANIA);
      PCK_PREPARAR_ANO.PR_COPIAR_CENTRO_COSTO(TABLA(MI_POS).COMPANIA, TABLA(MI_POS).NUMERO, MI_ANIO_ANT, TABLA(MI_POS).COMPANIA);   
      PCK_PREPARAR_ANO.PR_COPIAR_REFERENCIA(TABLA(MI_POS).COMPANIA, TABLA(MI_POS).NUMERO, MI_ANIO_ANT, TABLA(MI_POS).COMPANIA);
      PCK_PREPARAR_ANO.PR_COPIAR_FUENTE_RECURSO(TABLA(MI_POS).COMPANIA, TABLA(MI_POS).NUMERO, MI_ANIO_ANT, TABLA(MI_POS).COMPANIA);
      PCK_PREPARAR_ANO.PR_COPIAR_DESTINORECURSOS(TABLA(MI_POS).COMPANIA, TABLA(MI_POS).NUMERO, MI_ANIO_ANT, TABLA(MI_POS).COMPANIA);
    ELSE
      -- Si no hay un año anterior, se omiten los procesos que dependen del año anterior
      NULL;  
    END IF;
  END IF;
END AFTER STATEMENT;

END AIU_ANO_ACME;
