CREATE OR REPLACE TRIGGER "BIUD_DETALLE_PLAN_COMPRAS"  
/*
  NAME              : BIUD_DETALLE_PLAN_COMPRAS
  AUTHORS           : SYSMAN
  AUTHOR MIGRACION  : Juan Carlos Rodríguez Amézquita
  DATE MIGRADOR     : 14/11/2017
  TIME              : 04:12 PM
  DESCRIPTION       : Trigger antes de insertar, actualizar  y  eliminar para la 
                      tabla DETALLE_PLAN_COMPRAS.
  MODIFIER          : 
  DATE MODIFIED     : 
  TIME              : 
*/
BEFORE DELETE OR INSERT OR UPDATE ON DETALLE_PLAN_COMPRAS 
FOR EACH ROW 
DECLARE
  MI_COMPANIA                               PCK_SUBTIPOS.TI_COMPANIA;
  MI_TIPO_CONTRATACION                      TIPOADJUDICACION.CODIGO%TYPE;
  -- Valor asigando por defecto para el tipo de adjudicación en el detalle del plan de compras
  MI_TIPOADJUDICACION_DEFAULT               DETALLE_PLAN_COMPRAS.TIPO_CONTRATACION%TYPE := '3';
BEGIN

IF PCK_GENERALES.FC_CONS_CAMBIONIT() IN (0) THEN --VALIDACION PARA CUANDO SE ESTA CAMBIANDO EL NIT AL TERCERO
  -- Captura de datos
  IF INSERTING THEN
    MI_COMPANIA := :NEW.COMPANIA;
    MI_TIPO_CONTRATACION := :NEW.TIPO_CONTRATACION;
  ELSIF UPDATING OR DELETING THEN
    MI_COMPANIA := :OLD.COMPANIA;
    MI_TIPO_CONTRATACION := :OLD.TIPO_CONTRATACION;
  END IF;
  --
  IF INSERTING OR UPDATING THEN 
    -- Valida Tipo de Contratación
    IF MI_TIPO_CONTRATACION IS NULL OR MI_TIPO_CONTRATACION = MI_TIPOADJUDICACION_DEFAULT THEN
      PCK_PLANEACION_COM1.PR_EVALUAR_TIPO_ADJUD_PREDET(
        UN_COMPANIA => MI_COMPANIA
      , UN_VALOR_PREDETERMINADO => MI_TIPOADJUDICACION_DEFAULT
      );
    END IF;
  END IF;
  
END IF;  
END;