CREATE OR REPLACE TRIGGER "CIUD_DETALLE_COMPR_PPTAL_SALDO"  
FOR INSERT OR UPDATE OF COMPANIA
                      , ANO
                      , TIPO_CPTE
                      , COMPROBANTE
                      , CUENTA
                      , VALOR_DEBITO
                      , VALOR_CREDITO
ON DETALLE_COMPROBANTE_PPTAL
REFERENCING OLD AS OLD NEW AS NEW
COMPOUND TRIGGER 
    MI_RTA                 PCK_SUBTIPOS.TI_RTA_ACME;
    TYPE REGISTRO IS RECORD 
    (
        COMPANIA            PCK_SUBTIPOS.TI_COMPANIA,
        ANO                 PCK_SUBTIPOS.TI_ANIO,
        TIPO_CPTE           PCK_SUBTIPOS.TI_TIPOCOMPROBANTE,
        COMPROBANTE         PCK_SUBTIPOS.TI_NUMEROCOMPROBANTECNT
    );  
  TYPE REGISTROS IS TABLE OF REGISTRO INDEX BY BINARY_INTEGER ;
  TABLACT REGISTROS;
  MI_POSACT NUMBER DEFAULT 0;
  RTA       NUMBER;
AFTER EACH ROW IS
    BEGIN
        MI_POSACT := MI_POSACT + 1;
        TABLACT(MI_POSACT).COMPANIA            := :NEW.COMPANIA;
        TABLACT(MI_POSACT).ANO                 := :NEW.ANO;
        TABLACT(MI_POSACT).TIPO_CPTE           := :NEW.TIPO_CPTE;
        TABLACT(MI_POSACT).COMPROBANTE         := :NEW.COMPROBANTE;   
    END AFTER EACH ROW;
AFTER STATEMENT IS
    BEGIN
        FOR I IN 1..MI_POSACT LOOP
            --SE CONDICIONA PUES EN ENTIDADES COMO LA UPC AL MOMENTO DE CARGAR LOS PLANOS SE BLOQUEA EL SISTEMA
            -- PUES SE REALIZA A NIVEL DEL DETALLE LO QUE GENERA UNA REDUNDANCIA TAN GRANDE COMO REGISTROS TENGA LA TRANSACCION
            IF PCK_SYSMAN_UTL.FC_PAR(TABLACT(I).COMPANIA,'CONGELAR SALDO EN DETALLE PRESUPUESTAL',1,SYSDATE)='SI' THEN
                PCK_PRESUPUESTO3.PR_CONGELARSALDOENDETALLE(UN_COMPANIA           => TABLACT(I).COMPANIA,
                                                           UN_ANIO               => TABLACT(I).ANO,
                                                           UN_TIPO               => TABLACT(I).TIPO_CPTE,
                                                           UN_COMPROBANTEINICIAL => TABLACT(I).COMPROBANTE,
                                                           UN_COMPROBANTEFINAL   => TABLACT(I).COMPROBANTE);
            END IF;
        END LOOP;        
    END AFTER STATEMENT;
END;