*/
BEFORE UPDATE OF MOVIMIENTO ON AUXILIAR
FOR EACH ROW
  
BEGIN
  -- IMPIDE MODIFICAR EL MOVIMIENTO AL AUXILIAR VARIOS
  BEGIN  
    IF :OLD.CODIGO = PCK_DATOS.CONS_AUXILIAR THEN
      IF :NEW.MOVIMIENTO NOT IN(-1) THEN
          RAISE PCK_EXCEPCIONES.EXC_GENERAL;
      END IF;
    END IF;
  EXCEPTION WHEN PCK_EXCEPCIONES.EXC_GENERAL THEN  
    PCK_ERR_MSG.RAISE_WITH_MSG(
                 UN_EXC_COD    => SQLCODE,
                 UN_TABLAERROR => 'AUXILIAR',
                 UN_ERROR_COD  => PCK_ERRORES.ERROR_GRAL_ACTU_AUXVARIOS
               );
  END;
END;
