AFTER STATEMENT IS
  BEGIN
    FOR INTI IN 1 .. MI_POS LOOP
        SELECT 
           SUM(CASE WHEN NATURALEZA='D' 
                    THEN APROPIACION_DEBITO-APROPIACION_CREDITO 
                    ELSE APROPIACION_CREDITO-APROPIACION_DEBITO 
               END) SAPROPIADO,
           SUM(CASE WHEN NATURALEZA='D' 
                    THEN TRASLADO_DEBITO-TRASLADO_CREDITO 
                    ELSE TRASLADO_CREDITO-TRASLADO_DEBITO 
               END) STRASLADO,
           SUM(DISPONIBILIDAD) SDISPONIBLE,
           SUM(REDUCCION) SREDUCCION,
           SUM(ADICION) SADICION,
           SUM(REINTEGRO) SREINTEGRO,
           SUM(CASE WHEN NATURALEZA='D' 
                    THEN APLAZAM_DEBITO-APLAZAM_CREDITO 
                    ELSE APLAZAM_CREDITO-APLAZAM_DEBITO 
               END) SAPLAZAMIENTO,
           SUM(REG_CONTRACT+REG_NO_CONTRACT+MODIF_REG_CONT+MODIF_REG_NOCONT)  SRESERVAS
        INTO
             TABLA(INTI).MI_APROPIADO,
             TABLA(INTI).MI_TRASLADO ,
             TABLA(INTI).MI_DISPONIBLE,
             TABLA(INTI).MI_REDUCCION  ,
             TABLA(INTI).MI_ADICION    ,
             TABLA(INTI).MI_REINTEGRO  ,
             TABLA(INTI).MI_APLAZAMIENTO,
             TABLA(INTI).MI_RESERVAS  
        FROM SALDO_AUX_PPTAL
        WHERE  COMPANIA       =TABLA(INTI).MI_COMPANIA          
          AND  ANO            =TABLA(INTI).MI_ANIO                 
          AND  MES            BETWEEN 0 AND 13
          AND  CODIGO         =TABLA(INTI).MI_CUENTA               
          AND  CENTRO_COSTO   =TABLA(INTI).MI_CENTRO_COSTO         
          AND  TERCERO        =TABLA(INTI).MI_TERCERO              
          AND  SUCURSAL       =TABLA(INTI).MI_SUCURSAL             
          AND  AUXILIAR       =TABLA(INTI).MI_AUXILIAR             
          AND  REFERENCIA     =TABLA(INTI).MI_REFERENCIA           
          AND  FUENTE_RECURSO =TABLA(INTI).MI_FUENTE_RECURSO;
    
         TABLA(INTI).MI_APRDEFINITIVA  :=TABLA(INTI).MI_APRDEFINITIVA   + TABLA(INTI).MI_APROPIADO;
         TABLA(INTI).MI_SALDODISPONIBLE:=TABLA(INTI).MI_SALDODISPONIBLE + TABLA(INTI).MI_APRDEFINITIVA;
         TABLA(INTI).MI_DISPON_NETA    :=TABLA(INTI).MI_DISPON_NETA     + TABLA(INTI).MI_DISPONIBLE;
    
    IF TABLA(INTI).MI_APROPIADO <0 THEN
      DECLARE
        MI_MSGERROR     PCK_SUBTIPOS.TI_CLAVEVALOR; 
      BEGIN
      RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;           
      EXCEPTION WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN 
          MI_MSGERROR(1).CLAVE := 'APROPIADO';
          MI_MSGERROR(1).VALOR := ABS(TABLA(INTI).MI_APROPIADO);
          PCK_ERR_MSG.RAISE_WITH_MSG(
                 UN_EXC_COD => SQLCODE,
                 UN_TABLAERROR=>'SALDO_AUX_PPTAL',
                 UN_ERROR_COD => PCK_ERRORES.ERR_PPTO_APROPIACIONNEGATIVA,
                 UN_REEMPLAZOS => MI_MSGERROR
               );
      END;       
    END IF;
    IF TABLA(INTI).MI_APRDEFINITIVA <0 THEN
      DECLARE
        MI_MSGERROR     PCK_SUBTIPOS.TI_CLAVEVALOR; 
      BEGIN
      RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;           
      EXCEPTION WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN 
          MI_MSGERROR(1).CLAVE := 'APROPIADO';
          MI_MSGERROR(1).VALOR := ABS(TABLA(INTI).MI_APRDEFINITIVA);
          PCK_ERR_MSG.RAISE_WITH_MSG(
                 UN_EXC_COD => SQLCODE,
                 UN_TABLAERROR=>'SALDO_AUX_PPTAL',
                 UN_ERROR_COD => PCK_ERRORES.ERR_PPTO_APROPIACIONDEFINNEG,
                 UN_REEMPLAZOS => MI_MSGERROR
               );
      END;   
      --RAISE_APPLICATION_ERROR(-20000,'LA APROPIACION DEFINITIVA (APROPIACION+ADICIONES-REDUCCIONES +/- TRASLADOS), NO PUEDE QUEDAR NEGATIVA.');
    END IF;
    IF TABLA(INTI).MI_SALDODISPONIBLE <0 THEN
      DECLARE
        MI_MSGERROR     PCK_SUBTIPOS.TI_CLAVEVALOR; 
      BEGIN
      RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;           
      EXCEPTION WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN 
          MI_MSGERROR(1).CLAVE := 'SALDO';
          MI_MSGERROR(1).VALOR := ABS(TABLA(INTI).MI_SALDODISPONIBLE);
          PCK_ERR_MSG.RAISE_WITH_MSG(
                 UN_EXC_COD => SQLCODE,
                 UN_TABLAERROR=>'SALDO_AUX_PPTAL',
                 UN_ERROR_COD => PCK_ERRORES.ERR_PPTO_SALDODEFINITIVO,
                 UN_REEMPLAZOS => MI_MSGERROR
               );
      END;  
      --RAISE_APPLICATION_ERROR(-20000,'EL SALDO DISPONIBLE ((APROPIACION+ADICIONES-REDUCCIONES +/- TRASLADOS+REINTEGROS-APLAZAMIENTOS), NO PUEDE QUEDAR NEGATIVO.');
    END IF;
    IF TABLA(INTI).MI_DISPON_NETA <0 THEN
      DECLARE
        MI_MSGERROR     PCK_SUBTIPOS.TI_CLAVEVALOR; 
      BEGIN
      RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;           
      EXCEPTION WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN 
          MI_MSGERROR(1).CLAVE := 'DISPONIBILIDAD';
          MI_MSGERROR(1).VALOR := ABS(TABLA(INTI).MI_SALDODISPONIBLE);
          PCK_ERR_MSG.RAISE_WITH_MSG(
                 UN_EXC_COD => SQLCODE,
                 UN_TABLAERROR=>'SALDO_AUX_PPTAL',
                 UN_ERROR_COD => PCK_ERRORES.ERR_PPTO_SALDODEFINITIVO,
                 UN_REEMPLAZOS => MI_MSGERROR
               );
      END;  
      -- RAISE_APPLICATION_ERROR(-20000,'LA DISPONIBILIDD NETA (DISPONIBLE-RESERVAS), NO PUEDE QUEDAR NEGATIVO.');
    END IF;
    
    END LOOP;
  END AFTER STATEMENT;
END;
