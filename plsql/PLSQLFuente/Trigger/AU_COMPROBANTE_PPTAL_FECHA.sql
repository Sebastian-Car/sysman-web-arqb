CREATE OR REPLACE TRIGGER "AU_COMPROBANTE_PPTAL_FECHA"  FOR UPDATE OF FECHA ON COMPROBANTE_PPTAL 
COMPOUND TRIGGER

  MI_COMPANIA VARCHAR2(32000);
  MI_NUMERO   NUMBER;
  MI_EXISTE   NUMBER:=0;
  MI_CAMPOS   VARCHAR2(32000);
  MI_VALORES  VARCHAR2(32000);
  MI_RTA      NUMBER;  
  
  -- ES COMPUESTO POR QUE SE CONSULTA LA TABLA Y SE CREA UNA TABLA MUTANTE
-- CON ESTE TIPO GUARDO LOS REGISTROS POR CADA REGISTRO DEL UPDATE PARA LUEGO PROCESARLO
  TYPE REGISTRO IS RECORD 
  (
  COMPANIA                  PCK_SUBTIPOS.TI_COMPANIA,
  ANO                       PCK_SUBTIPOS.TI_ANIO,
  TIPO                      PCK_SUBTIPOS.TI_TIPOCOMPROBANTEPPTAL,
	COMPROBANTE               PCK_SUBTIPOS.TI_NUMEROCOMPROBANTECNT,
  FECHA                     DATE
	);  
  TYPE REGISTROS IS TABLE OF REGISTRO INDEX BY BINARY_INTEGER ;
  TABLA REGISTROS;
  MI_POS NUMBER DEFAULT 0;
  
AFTER EACH ROW IS

BEGIN
  --POR CADA FILA SE CARGA LA TABLA PARA LUEGO LARZAR EL REGISTRO
  
  IF UPDATING THEN
    MI_POS := MI_POS+1;
    TABLA(MI_POS).COMPANIA     := :NEW.COMPANIA;  
    TABLA(MI_POS).ANO          := :NEW.ANO;  
    TABLA(MI_POS).TIPO         := :NEW.TIPO;  
    TABLA(MI_POS).COMPROBANTE  := :NEW.NUMERO;  
    TABLA(MI_POS).FECHA        := :NEW.FECHA;  
  END IF;
END AFTER EACH ROW;

AFTER STATEMENT IS

BEGIN
  FOR i IN 1..MI_POS LOOP
    PCK_PRESUPUESTO3.PR_CAMBIARFECHA (     UN_COMPANIA    => TABLA(MI_POS).COMPANIA,
                                           UN_ANO         => TABLA(MI_POS).ANO,
                                           UN_TIPO        => TABLA(MI_POS).TIPO,
                                           UN_COMPROBANTE => TABLA(MI_POS).COMPROBANTE,
                                           UN_FECHA       => TABLA(MI_POS).FECHA);   
  END LOOP;  
END AFTER STATEMENT;
END AU_COMPROBANTE_PPTAL_FECHA ;
