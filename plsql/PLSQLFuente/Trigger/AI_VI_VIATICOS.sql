FOR INSERT ON VI_VIATICOS

COMPOUND TRIGGER
TYPE REGISTRO IS RECORD
(

  COMPANIA         PCK_SUBTIPOS.TI_COMPANIA,
	ANO              PCK_SUBTIPOS.TI_ENTERO,
  TIPOVIATICO      VI_VIATICOS.TIPO_VIATICO%TYPE, 
	CODIGOSOLICITUD  VI_VIATICOS.CODSOLICITUD%TYPE, 
  TERCERO          VI_VIATICOS.TERCERO%TYPE, 	
  SUCURSAL         VI_VIATICOS.SUCURSAL%TYPE, 
  PAISORIGEN       VI_VIATICOS.PAIS_ORIGEN%TYPE,  
  DEPTOORIGEN      VI_VIATICOS.DEPARTAMENTO_ORIGEN%TYPE, 
  CIUDADORIGEN     VI_VIATICOS.CIUDAD_ORIGEN%TYPE, 
  PAISDESTINO      VI_VIATICOS.PAIS_DESTINO%TYPE, 
  DEPTODESTINO     VI_VIATICOS.DEPARTAMENTO_DESTINO%TYPE, 
  CIUDADDESTINO    VI_VIATICOS.CIUDAD_DESTINO%TYPE, 
  DESCRIPCION      VI_VIATICOS.OBSERVACION%TYPE,  
  USUARIO          PCK_SUBTIPOS.TI_USUARIO
  
);
TYPE REGISTROS IS TABLE OF REGISTRO INDEX BY BINARY_INTEGER ;
  TABLAAUX REGISTROS;
  MI_POSACT NUMBER DEFAULT 0;

AFTER EACH ROW IS
BEGIN
  MI_POSACT := MI_POSACT+1;
  TABLAAUX(MI_POSACT).COMPANIA        := :NEW.COMPANIA;
  TABLAAUX(MI_POSACT).ANO             := :NEW.ANO;
  TABLAAUX(MI_POSACT).TIPOVIATICO     := :NEW.TIPO_VIATICO;
  TABLAAUX(MI_POSACT).CODIGOSOLICITUD := :NEW.CODSOLICITUD;
  TABLAAUX(MI_POSACT).TERCERO         := :NEW.TERCERO;
  TABLAAUX(MI_POSACT).SUCURSAL        := :NEW.SUCURSAL;
  TABLAAUX(MI_POSACT).PAISORIGEN      := :NEW.PAIS_ORIGEN;
  TABLAAUX(MI_POSACT).DEPTOORIGEN     := :NEW.DEPARTAMENTO_ORIGEN;
  TABLAAUX(MI_POSACT).CIUDADORIGEN    := :NEW.CIUDAD_ORIGEN;
  TABLAAUX(MI_POSACT).PAISDESTINO     := :NEW.PAIS_DESTINO;
  TABLAAUX(MI_POSACT).DEPTODESTINO    := :NEW.DEPARTAMENTO_DESTINO;
  TABLAAUX(MI_POSACT).CIUDADDESTINO   := :NEW.CIUDAD_DESTINO;
  TABLAAUX(MI_POSACT).DESCRIPCION     := :NEW.OBSERVACION;
  TABLAAUX(MI_POSACT).USUARIO         := :NEW.CREATED_BY;
  

END AFTER EACH ROW;

AFTER STATEMENT IS
BEGIN
  FOR i IN 1..MI_POSACT LOOP
    PCK_VIATICOS.PR_INSDETALLEVIATICOS(UN_COMPANIA     => TABLAAUX(MI_POSACT).COMPANIA ,
                                    UN_ANO             =>  TABLAAUX(MI_POSACT).ANO,
                                    UN_TIPOVIATICO     => TABLAAUX(MI_POSACT).TIPOVIATICO, 
                                    UN_CODIGOSOLICITUD => TABLAAUX(MI_POSACT).CODIGOSOLICITUD, 
                                    UN_TERCERO         => TABLAAUX(MI_POSACT).TERCERO , 	
                                    UN_SUCURSAL        => TABLAAUX(MI_POSACT).SUCURSAL  , 
                                    UN_PAISORIGEN      => TABLAAUX(MI_POSACT).PAISORIGEN,  
                                    UN_DEPTOORIGEN     => TABLAAUX(MI_POSACT).DEPTOORIGEN, 
                                    UN_CIUDADORIGEN    => TABLAAUX(MI_POSACT).CIUDADORIGEN, 
                                    UN_PAISDESTINO     => TABLAAUX(MI_POSACT).PAISDESTINO, 
                                    UN_DEPTODESTINO    => TABLAAUX(MI_POSACT).DEPTODESTINO, 
                                    UN_CIUDADDESTINO   => TABLAAUX(MI_POSACT).CIUDADDESTINO, 
                                    UN_DESCRIPCION     => TABLAAUX(MI_POSACT).DESCRIPCION,                        
                                    UN_USUARIO         => TABLAAUX(MI_POSACT).USUARIO );
    
  END LOOP;
  END AFTER STATEMENT;
      
    


END;
