  DATE              : 10/04/2025
  TICKET            : 1326
  DESCRIPTION       : SE CREA TRIGGER CON LA FINALIDAD DE ACTUALIZAR EL CARGO EN LA TABLA DEPENDENCIA RESPONSABLE 
                      CUANDO SE LE ACTUALICE EL MISMO EN AL RESPONSABLE
*/
AFTER UPDATE OF CARGO ON RESPONSABLE
FOR EACH ROW
BEGIN
  UPDATE DEPENDENCIA_RESPONSABLE
    SET CARGO = :NEW.CARGO
  WHERE COMPANIA = :NEW.COMPANIA
    AND RESPONSABLE = :NEW.CEDULA
    AND SUCURSAL = :NEW.SUCURSAL;
END;