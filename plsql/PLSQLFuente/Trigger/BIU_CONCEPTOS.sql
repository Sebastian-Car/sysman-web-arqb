BEFORE INSERT OR UPDATE OF ID_DE_CONCEPTO, CLASE ON CONCEPTOS 
FOR EACH ROW
BEGIN

 IF (:NEW.ID_DE_CONCEPTO BETWEEN 1600 AND 1698) AND :NEW.CLASE <> 5 THEN
    BEGIN
        --El concepto que intenta ingresar debe ser de clase DESCUENTO
        RAISE PCK_EXCEPCIONES.EXC_NOMINA;    
        EXCEPTION WHEN PCK_EXCEPCIONES.EXC_NOMINA THEN
        PCK_ERR_MSG.RAISE_WITH_MSG(UN_EXC_COD   => SQLCODE
                                ,UN_ERROR_COD => PCK_ERRORES.ERR_CONCEPTODESCUENTO);
    END; 
 END IF;

 IF :NEW.ID_DE_CONCEPTO > PCK_NOMINA.MAXI THEN
    BEGIN
    --El código de concepto que intenta crear excede el máximo permitido
      RAISE PCK_EXCEPCIONES.EXC_NOMINA;

    EXCEPTION WHEN PCK_EXCEPCIONES.EXC_NOMINA THEN
      PCK_ERR_MSG.RAISE_WITH_MSG(UN_EXC_COD   => SQLCODE
                                ,UN_ERROR_COD => PCK_ERRORES.ERR_CONCEPTOSOBRELIMITE);
    END; 
 END IF;

