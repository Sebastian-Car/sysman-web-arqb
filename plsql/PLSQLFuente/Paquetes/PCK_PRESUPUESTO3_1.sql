            END IF;
       END IF;
      END IF;
    ELSE
      IF UN_ACCION                                                                                       = 'E' THEN
          MI_VALIDACION_R                                                                               := -1;
      ELSE 
        IF MI_VALSOLICITUDAFECT >0 THEN 
           IF  UN_VALOR_DIGITADO < UN_VALOR_ANTIGUO THEN 
               IF (MI_VALSOLICITUDAFECT - ( NVL(UN_VALOR_DIGITADO,0) -  NVL(UN_VALOR_ANTIGUO,0)))  >= 0 THEN
                    MI_VALIDACION_R := -1;
               ELSE
                  MI_VALIDACION_R := 0;
               END IF;
           ELSE 
               IF (MI_VALSOLICITUDAFECT -  NVL(UN_VALOR_DIGITADO,0) )  >= 0 THEN
                    MI_VALIDACION_R := -1;
               ELSE
                  MI_VALIDACION_R := 0;
               END IF;
           END IF;
        ELSE
           MI_VALIDACION_R := 0;
        END IF;
      END IF;
    END IF;
    BEGIN
      BEGIN
        MI_TABLA     := 'BP_PLAN_ADQUISICIONES_VIGENCIA';
        MI_CONDICION := '   COMPANIA         = ''' || UN_COMPANIA || '''                                    
AND VIGENCIA_INICIAL = ' || UN_VIGENCIA_ITEM_AFECTADO||'                                    
AND ID_PLAN          = ''' || UN_ITEM_AFECTADO ||'''                                    
AND CODIGO           =' || UN_CODIGO_ITEM ||'';
        MI_RTA       := PCK_DATOS.FC_ACME(UN_ACCION => 'M', UN_TABLA => MI_TABLA, UN_CAMPOS => MI_CAMPOS, UN_CONDICION => MI_CONDICION);
      EXCEPTION
      WHEN PCK_EXCEPCIONES.EXC_ACTUALIZAR THEN
        MI_MSGERROR(1).CLAVE := 'ITEM';
        MI_MSGERROR(1).VALOR := UN_CODIGO_ITEM;
        RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;
      END;
    EXCEPTION
    WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN
      PCK_ERR_MSG.RAISE_WITH_MSG(UN_EXC_COD => SQLCODE, UN_TABLAERROR => MI_TABLA, UN_ERROR_COD => PCK_ERRORES.ERR_PPTO_ACT_TOTAL_SOLICITUD, UN_REEMPLAZOS => MI_MSGERROR);
    END;
    MI_SALIDA_AFEC := PCK_PRESUPUESTO3.FC_VALIDACIONES_AFECTADO(UN_COMPANIA    => UN_COMPANIA,
                                                           UN_VALOR_DIGITADO => UN_VALOR_DIGITADO,
                                                           UN_TIPO_SOLICITUD        => UN_TIPO_SOLICITUD,
                                                           UN_ACCION => UN_ACCION,
                                                           UN_CODIGO => UN_CODIGO,
                                                           UN_ANO      => UN_ANO,
                                                           UN_NRO_SDP       => UN_NRO_SDP);
   
    IF MI_VALIDACION_R    = -1 AND MI_VALIDACION_I = -1 THEN
      MI_SALIDA          := -1;
    ELSIF MI_VALIDACION_R = 0 AND MI_VALIDACION_I = 0 THEN
      MI_SALIDA          := 0;
    ELSIF MI_VALIDACION_R = -1 AND MI_VALIDACION_I = 0 THEN
      MI_SALIDA          := 2;
    ELSIF MI_VALIDACION_R = 0 AND MI_VALIDACION_I = -1 THEN
      MI_SALIDA          := 3;
    ELSIF MI_VALIDACION_R = 4 OR MI_VALIDACION_I = 4 THEN
       IF  MI_VALIDACION_R =  -1 THEN 
          MI_SALIDA          := -1;
      ELSE
          MI_SALIDA          := 4;
      END IF ;
    END IF;     

 
     IF (UN_ACCION = 'M' OR UN_ACCION= 'E')THEN  
            IF MI_SALIDA = -2 OR MI_SALIDA = -1 OR MI_SALIDA = 2 THEN
                IF MI_SALIDA_AFEC = 0 THEN
                     RETURN MI_SALIDA;
                  ELSE 
                  MI_SALIDA := MI_SALIDA_AFEC;
                 END IF; 
            ELSE
              RETURN MI_SALIDA;
            END IF;
     END IF;
     
        RETURN MI_SALIDA;
        
    ELSE 
    --Ticket#7735417: Se crea proceso que valida si el saldo del rubro es mayor a lo solicitado.
    --Calcula el saldo del rubro con sus auxiliares.
    BEGIN
        SELECT SUM(S.APROPIACION_DEBITO + S.ADICION+S.REDUCCION +(S.TRASLADO_DEBITO -S.TRASLADO_CREDITO) +(S.APLAZAM_DEBITO - S.APLAZAM_CREDITO)- S.DISPONIBILIDAD) SALDO
        INTO MI_SALDO_RUBRO
        FROM SALDO_AUX_PPTAL S   
        WHERE S.COMPANIA = UN_COMPANIA  
        AND S.ANO = UN_ANO  
        AND S.CODIGO = UN_CODIGO 
        AND S.FUENTE_RECURSO = UN_FUENTE
        AND S.CENTRO_COSTO = UN_CENTRO_COSTO
        AND S.REFERENCIA = UN_REFERENCIA
        AND S.MES <= 12;
    END;
    --Calcula si el rubro con sus axuliares fue utilizado en el mismo registro.
    BEGIN
      SELECT NVL(SUM(NVL(D_SOLICITUDDISPONIBILIDAD.VALOR,0)),0) SUMA
      INTO MI_SUMA_RUBRO
      FROM D_SOLICITUDDISPONIBILIDAD
      WHERE D_SOLICITUDDISPONIBILIDAD.COMPANIA      = UN_COMPANIA
      AND D_SOLICITUDDISPONIBILIDAD.ANO             = UN_ANO
      AND D_SOLICITUDDISPONIBILIDAD.NUMERO          = UN_NRO_SDP
      AND D_SOLICITUDDISPONIBILIDAD.RUBRO          = UN_CODIGO
      AND D_SOLICITUDDISPONIBILIDAD.FUENTE          = UN_FUENTE
      AND D_SOLICITUDDISPONIBILIDAD.CENTRO_COSTO          = UN_CENTRO_COSTO
      AND D_SOLICITUDDISPONIBILIDAD.REFERENCIA          = UN_REFERENCIA;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            MI_SUMA_RUBRO := 0;
        END;
        
        IF UN_ACCION= 'I' THEN
            IF  MI_SALDO_RUBRO >= (UN_VALOR_DIGITADO + MI_SUMA_RUBRO) THEN
                MI_SALIDA          := -1;
            ELSE 
                MI_SALIDA          := 0;
            END IF;
        
        ELSIF UN_ACCION = 'M' THEN  
            IF  MI_SALDO_RUBRO >= ((MI_SUMA_RUBRO - UN_VALOR_ANTIGUO) + UN_VALOR_DIGITADO) THEN
                MI_SALIDA          := -2;
            ELSE 
                MI_SALIDA          := 0;
            END IF;
        ELSE
            MI_SALIDA          := -1;
        END IF; 
       
       RETURN MI_SALIDA; 
    
    END IF;
        
  END FC_ACTUALIZAR_VALOR_SOLICITADO;
