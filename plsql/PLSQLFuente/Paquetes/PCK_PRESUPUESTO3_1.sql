    /*
    NAME              : FC_GETNOMBRERUBROPREDECESORES
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : SERGIO ESTEBAN PIÑA VARGAS
    DATE MIGRADOR     : 16/11/2017
    TIME              : 12:24 PM
    SOURCE MODULE     :
    MODIFIER          :
    DATE MODIFIED     :
    TIME              :
    DESCRIPTION       : recibe el codigo y retorna el nombre de los rubros predecesores
    @NAME:  getNombreRubroPredecesores
    */
    (
      UN_COMPANIA IN PCK_SUBTIPOS.TI_COMPANIA,
      UN_ANIO     IN PCK_SUBTIPOS.TI_ANIO,
      UN_RUBRO    IN PCK_SUBTIPOS.TI_CODIGOPPTAL)
    RETURN VARCHAR2
  AS
    MI_NOMBRE PCK_SUBTIPOS.TI_RTA_ACME;
  BEGIN
    BEGIN
      FOR RS IN
      (SELECT NOMBRE
      FROM PLAN_PRESUPUESTAL
      WHERE COMPANIA = ''
        || UN_COMPANIA
        || ''
      AND ANO            = UN_ANIO
      AND CODIGO         = SUBSTR(UN_RUBRO, 0, LENGTH(CODIGO))
      AND LENGTH(CODIGO) > 5
      )
      LOOP
        MI_NOMBRE := MI_NOMBRE || RS.NOMBRE || ' / ';
      END LOOP;
      IF MI_NOMBRE IS NOT NULL THEN
        MI_NOMBRE  := SUBSTR(MI_NOMBRE, 0, LENGTH(MI_NOMBRE) -2);
      END IF;
    END;
    RETURN MI_NOMBRE ;
  END;
