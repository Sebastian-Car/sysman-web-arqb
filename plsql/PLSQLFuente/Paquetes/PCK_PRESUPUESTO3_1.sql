  PROCEDURE PR_CONGELARSALDOENDETALLE(
      /*
      NAME              : PR_CONGELARSALDOENDETALLE
      AUTHORS           : SYSMAN  SAS
      AUTHOR MIGRACION  : JOSÉ PASCUAL GÓMEZ BLANCO
      DATE MIGRADOR     : 18/04/2018
      TIME              : 02:04 PM
      SOURCE MODULE     : Presupuesto
      MODIFIER          :
      DATE MODIFIED     :
      TIME              :
      DESCRIPTION       : Se crea nuevo para ajustar los valores de saldos de la tabla detalle_comprobante_pptal
      de acuerdo a la fecha del comprobante para afectos de informes - No se tiene en cuenta
      los campos de auditoria debido a que esta rutina corresponde a un mantenimiento
      @NAME:  congelarSaldoDetalle
      @METHOD:  GET
      */
      UN_COMPANIA           IN PCK_SUBTIPOS.TI_COMPANIA,
      UN_ANIO               IN PCK_SUBTIPOS.TI_ANIO,
      UN_TIPO               IN PCK_SUBTIPOS.TI_TIPOCOMPROBANTEPPTAL,
      UN_COMPROBANTEINICIAL IN PCK_SUBTIPOS.TI_NUMEROCOMPROBANTEPPTAL,
      UN_COMPROBANTEFINAL   IN PCK_SUBTIPOS.TI_NUMEROCOMPROBANTEPPTAL )
  AS
    MI_SALDO PCK_SUBTIPOS.TI_DOBLE         :=0;
    MI_DISACUMULADAS PCK_SUBTIPOS.TI_DOBLE :=0;
    MI_RESERVAS PCK_SUBTIPOS.TI_DOBLE      :=0;
    MI_CAMPOS PCK_SUBTIPOS.TI_CAMPOS;
    MI_CONDICION PCK_SUBTIPOS.TI_CONDICION;
    MI_RTA_ACME NUMBER;
  BEGIN
    FOR RS IN
    (SELECT CUENTA,
      CONSECUTIVO,
      FECHA,
      COMPROBANTE,
      TIPO_CPTE
    FROM DETALLE_COMPROBANTE_PPTAL
    WHERE COMPANIA = UN_COMPANIA
    AND ANO        = UN_ANIO
    AND TIPO_CPTE  = UN_TIPO
    AND COMPROBANTE BETWEEN UN_COMPROBANTEINICIAL AND UN_COMPROBANTEFINAL
    ORDER BY FECHA,
      COMPROBANTE,
      CONSECUTIVO,
      CUENTA
    )
    LOOP
      FOR RS1                      IN
      (SELECT SAL.APROPIACIONINICIAL+ SUM(
        CASE
          WHEN TIPOMODI.CLASE    IN ('DIS', 'ADD', 'DMD', 'APL')
          THEN CASE WHEN  PCK_SYSMAN_UTL.FC_PAR(COMP.COMPANIA,'MANEJA SALDO DISPONIBLE ANTES DE DISPONIBILIDAD',3,COMP.FECHA)='NO'
                   THEN COMP.VALOR_CREDITO - COMP.VALOR_DEBITO
                   ELSE CASE WHEN COMP.FECHA <= RS.FECHA AND  COMP.COMPROBANTE  < RS.COMPROBANTE AND COMP.TIPO_CPTE  = RS.TIPO_CPTE
                         THEN COMP.VALOR_CREDITO - COMP.VALOR_DEBITO
                         ELSE 
                         0
                         END 
                END   
          ELSE
            CASE
              WHEN TIPOMODI.CLASE   IN ('ADC', 'RED', 'TRA')
              THEN COMP.VALOR_DEBITO - COMP.VALOR_CREDITO
              ELSE 0
            END
        END) MI_SALDO,
        SUM(
        CASE
          WHEN TIPOMODI.CLASE   IN ('DIS', 'ADD', 'DMD')
          THEN COMP.VALOR_DEBITO - COMP.VALOR_CREDITO
          ELSE 0
        END) MI_DISACUMULADAS,
        SUM(
        CASE
          WHEN TIPOMODI.CLASE   IN ('RES', 'ADR', 'DMR')
          THEN COMP.VALOR_DEBITO - COMP.VALOR_CREDITO
          ELSE 0
        END) MI_RESERVAS
      FROM DETALLE_COMPROBANTE_PPTAL COMP
      INNER JOIN TIPO_COMPROBPP TIPOMODI
      ON COMP.COMPANIA   = TIPOMODI.COMPANIA
      AND COMP.TIPO_CPTE = TIPOMODI.CODIGO
      INNER JOIN PLAN_PRESUPUESTAL P
      ON P.COMPANIA = COMP.COMPANIA
      AND P.ANO     = COMP.ANO
      AND P.CODIGO  = COMP.CUENTA
      INNER JOIN SALDO_AUX_PPTAL SAL
      ON COMP.COMPANIA     = SAL.COMPANIA
      AND COMP.ANO         = SAL.ANO
      AND COMP.CUENTA      = SAL.CODIGO
      AND SAL.CENTRO_COSTO =
        CASE
          WHEN P.MAN_CEN_CTO IN(0)
          THEN PCK_DATOS.FC_CONS_CENTRO
          ELSE COMP.CENTRO_COSTO
        END
      AND SAL.TERCERO =
        CASE
          WHEN P.MAN_AUX_TER IN(0)
          THEN PCK_DATOS.FC_CONS_TERCERO
          ELSE COMP.TERCERO
        END
      AND SAL.SUCURSAL =
        CASE
          WHEN P.MAN_AUX_TER IN(0)
          THEN PCK_DATOS.FC_CONS_SUCURSAL
          ELSE COMP.SUCURSAL
        END
      AND SAL.AUXILIAR =
        CASE
          WHEN P.MAN_AUX_GEN IN(0)
          THEN PCK_DATOS.FC_CONS_AUXILIAR
          ELSE COMP.AUXILIAR
        END
      AND SAL.FUENTE_RECURSO =
        CASE
          WHEN P.MAN_AUX_FUE IN(0)
          THEN PCK_DATOS.FC_CONS_FUENTE
          ELSE COMP.FUENTE_RECURSO
        END
      AND SAL.REFERENCIA =
        CASE
          WHEN P.MAN_AUX_REF IN(0)
          THEN PCK_DATOS.FC_CONS_REFERENCIA
          ELSE COMP.REFERENCIA
        END
      WHERE COMP.COMPANIA   = UN_COMPANIA
      AND COMP.ANO          = UN_ANIO
      AND COMP.CUENTA       = RS.CUENTA
      AND ((COMP.FECHA     <= RS.FECHA )
      OR (COMP.FECHA        = RS.FECHA
      AND COMP.COMPROBANTE  < RS.COMPROBANTE
      AND COMP.TIPO_CPTE    = RS.TIPO_CPTE)
      OR (COMP.COMPROBANTE  = RS.COMPROBANTE
      AND COMP.CONSECUTIVO <= RS.CONSECUTIVO
      AND COMP.TIPO_CPTE    = RS.TIPO_CPTE))
      AND SAL.MES           = 0
      AND TIPOMODI.CLASE   IN ('DIS','ADC','APL','RED','TRA', 'ADD', 'DMD', 'ADR', 'DMR', 'RES')
      GROUP BY SAL.APROPIACIONINICIAL
      )
      LOOP
        BEGIN
          MI_CAMPOS    := ' DISACUMULADAS      =' || RS1.MI_DISACUMULADAS || 
                          ',RESACUMULADAS      =' || RS1.MI_RESERVAS      || 
                          ',APRDEFINITIVA      =' || (RS1.MI_DISACUMULADAS + RS1.MI_SALDO) || 
                          ',SALDO              =' || RS1.MI_SALDO || 
                          ',VALORCOMPROMISO    =' || (RS1.MI_DISACUMULADAS - RS1.MI_RESERVAS) || 
                          ',DISPONIBILIDADNETA =' || (RS1.MI_SALDO + RS1.MI_DISACUMULADAS - (RS1.MI_DISACUMULADAS - RS1.MI_RESERVAS)) ;
          MI_CONDICION := 'COMPANIA     =''' || UN_COMPANIA    || '''                             
                       AND ANO          ='   || UN_ANIO        || '                             
                       AND TIPO_CPTE    =''' || UN_TIPO        || '''                             
                       AND COMPROBANTE  ='   || RS.COMPROBANTE || '                             
                       AND CONSECUTIVO  ='   || RS.CONSECUTIVO || '                             
                       AND CUENTA       =''' || RS.CUENTA      || '''
                       AND (DISACUMULADAS      <>' || RS1.MI_DISACUMULADAS || '
                         OR RESACUMULADAS      <>' || RS1.MI_RESERVAS      || '
                         OR APRDEFINITIVA      <>' || (RS1.MI_DISACUMULADAS + RS1.MI_SALDO) || '
                         OR SALDO              <>' || RS1.MI_SALDO || '
                         OR VALORCOMPROMISO    <>' || (RS1.MI_DISACUMULADAS - RS1.MI_RESERVAS) || '
                         OR DISPONIBILIDADNETA <>' || (RS1.MI_SALDO + RS1.MI_DISACUMULADAS - (RS1.MI_DISACUMULADAS - RS1.MI_RESERVAS)) || '
                         )';
          MI_RTA_ACME  := PCK_DATOS.FC_ACME(UN_TABLA     => 'DETALLE_COMPROBANTE_PPTAL', 
                                            UN_ACCION    => 'M', 
                                            UN_CAMPOS    => MI_CAMPOS, 
                                            UN_CONDICION => MI_CONDICION);
        EXCEPTION
        WHEN PCK_EXCEPCIONES.EXC_ACTUALIZAR THEN
          RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;
        END;
      END LOOP;
    END LOOP;
  END PR_CONGELARSALDOENDETALLE;

