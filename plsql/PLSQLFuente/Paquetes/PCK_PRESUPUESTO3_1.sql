          MI_MSGERROR(1).CLAVE := 'TIPO_CPTE';
          MI_MSGERROR(1).VALOR := MI_RS.TIPO_CPTE;
          MI_MSGERROR(2).CLAVE := 'COMPROBANTE';
          MI_MSGERROR(2).VALOR := MI_RS.COMPROBANTE;
          MI_MSGERROR(3).CLAVE := 'CONSECUTIVO';
          MI_MSGERROR(3).VALOR := MI_RS.CONSECUTIVO;
          RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;
        END;
      EXCEPTION
      WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN
        PCK_ERR_MSG.RAISE_WITH_MSG(UN_EXC_COD => SQLCODE, UN_TABLAERROR => MI_TABLA, UN_ERROR_COD => PCK_ERRORES.ERR_AFECTACION_MODIF_CPTE, UN_REEMPLAZOS => MI_MSGERROR);
      END;
    END LOOP;

    -- ACTUALIZA AFECTACIONES DE PRIMER NIVEL

    FOR MI_RS IN
    (
      SELECT D.COMPANIA,
              D.ANO,
              D.TIPO_CPTE,
              D.COMPROBANTE,
              D.CONSECUTIVO,
              D.CUENTA,
              D.CENTRO_COSTO,
              D.AUXILIAR,
              D.FUENTE_RECURSO,
              D.VALOR_DEBITO,
              D.VALOR_CREDITO,
              D.DEBITO_AFECTADO,
              D.CREDITO_AFECTADO,
              Y.SDEBITOS,
              Y.SCREDITOS
      FROM DETALLE_COMPROBANTE_PPTAL D
      INNER JOIN
      (
        SELECT  X.COMPANIA,
                X.ANO_AFECT ANO,
                X.TIPO_CPTE_AFECT TIPO_CPTE,
                X.CMPTE_AFECTADO COMPROBANTE,
                X.CONSECUTIVOPPTO CONSECUTIVO,
        SUM( CASE WHEN NOT (C.AFECTACION ='A' OR C.AFECTACION ='R')
                THEN VALOR_DEBITO+MODIFICACION_DEBITO
              ELSE 0
            END) SDEBITOS,
        SUM( CASE WHEN NOT (C.AFECTACION ='A' OR C.AFECTACION ='R')
                THEN VALOR_CREDITO+MODIFICACION_CREDITO
              ELSE 0
            END) SCREDITOS
      FROM DETALLE_COMPROBANTE_PPTAL X
      INNER JOIN TIPO_COMPROBPP T
        ON X.COMPANIA  =T.COMPANIA
        AND X.TIPO_CPTE=T.CODIGO
      INNER JOIN CLASECNTPRES C
        ON (T.CLASE = C.CODIGO)
      WHERE X.TIPO_CPTE_AFECT IS NOT NULL
        AND X.CMPTE_AFECTADO    IS NOT NULL
      GROUP BY  X.COMPANIA,
                X.ANO_AFECT,
                X.TIPO_CPTE_AFECT,
                X.CMPTE_AFECTADO,
                X.CONSECUTIVOPPTO
      ) Y 
      ON D.COMPANIA     = Y.COMPANIA
        AND D.ANO         = Y.ANO
        AND D.TIPO_CPTE   = Y.TIPO_CPTE
        AND D.COMPROBANTE = Y.COMPROBANTE
        AND D.CONSECUTIVO = Y.CONSECUTIVO
      WHERE D.COMPANIA    = UN_COMPANIA
        AND D.ANO           = UN_ANO
        AND ( DEBITO_AFECTADO <> SDEBITOS OR CREDITO_AFECTADO <> SCREDITOS )
    )
    LOOP
      BEGIN
        BEGIN
          MI_CAMPOS    := 'DEBITO_AFECTADO = '||MI_RS.SDEBITOS||',                    
                          CREDITO_AFECTADO = '||MI_RS.SCREDITOS||'';

          MI_TABLA     := 'DETALLE_COMPROBANTE_PPTAL';

          MI_CONDICION := 'COMPANIA       = '''||MI_RS.COMPANIA||'''     
                          AND ANO         = '|| MI_RS.ANO ||'     
                          AND TIPO_CPTE   = '''||MI_RS.TIPO_CPTE||'''     
                          AND COMPROBANTE = '||MI_RS.COMPROBANTE ||'     
                          AND CONSECUTIVO = '||MI_RS.CONSECUTIVO||'';

          MI_RTA       := PCK_DATOS.FC_ACME(UN_ACCION => 'M', UN_TABLA => MI_TABLA, UN_CAMPOS => MI_CAMPOS, UN_CONDICION => MI_CONDICION);

        EXCEPTION
        WHEN PCK_EXCEPCIONES.EXC_ACTUALIZAR THEN
          MI_MSGERROR(1).CLAVE := 'TIPO_CPTE';
          MI_MSGERROR(1).VALOR := MI_RS.TIPO_CPTE;
          MI_MSGERROR(2).CLAVE := 'COMPROBANTE';
          MI_MSGERROR(2).VALOR := MI_RS.COMPROBANTE;
          MI_MSGERROR(3).CLAVE := 'CONSECUTIVO';
          MI_MSGERROR(3).VALOR := MI_RS.CONSECUTIVO;
