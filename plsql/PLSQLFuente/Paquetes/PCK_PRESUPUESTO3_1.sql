'''||UN_TERCERO||''',
'''||UN_SUCURSAL||''',
'''||RS.AUXILIAR||''',
'''||RS.FUENTE_RECURSO||''',
'''||RS.CONTRACTUAL||''',
'||RS.PAPELES||',
'''||RS.SECTOR||''',
'''||RS.PROGRAMA||''',
'''||RS.SUBPROGRAMA||''',
'''||RS.COD_PROD_CUIPO||''',
'''||RS.CODIGO_BPIN||''',
'''||RS.CODIGO_CCPET||''',
