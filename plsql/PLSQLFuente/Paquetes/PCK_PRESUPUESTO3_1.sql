  FUNCTION FC_ENUMERAR
    /*
    NAME              : FC_ENUMERAR
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : JAVIER VILLATE
    DATE MIGRADOR     : 26/05/2017
    TIME              : 13:04 PM
    SOURCE MODULE     : Presupuesto
    MODIFIER          :
    DATE MODIFIED     :
    TIME              :
    DESCRIPTION       : Retorna el consecutivo correspondiente para creación de comprobante de presupuesto
    @NAME:  enumerar
    @METHOD:  GET
    */
    (
      UN_COMPANIA IN PCK_SUBTIPOS.TI_COMPANIA,
      UN_ANO      IN PCK_SUBTIPOS.TI_ANIO,
      UN_TIPO     IN PCK_SUBTIPOS.TI_TIPOCOMPROBANTEPPTAL )
    RETURN NUMBER
  AS
    MI_CONSECUTIVO NUMBER := 0;
    MI_RTA         VARCHAR2(32000);
  BEGIN
    IF UN_TIPO           <> '' OR UN_TIPO IS NOT NULL THEN
      MI_CONSECUTIVO     := PCK_SYSMAN_UTL.FC_GENCONSECUTIVO('COMPROBANTE_PPTAL', 'COMPANIA = '''|| UN_COMPANIA ||'''                                 
                                                              AND ANO   = ' || UN_ANO ||'                                
                                                              AND TIPO  = ''' || UN_TIPO ||'''', 'NUMERO', '0');
      IF (MI_CONSECUTIVO  =0 OR MI_CONSECUTIVO = 1) THEN
        MI_CONSECUTIVO   := PCK_SYSMAN_UTL.FC_GENCONSECUTIVO('CONSECUTIVOTCP', 'COMPANIA = '''|| UN_COMPANIA ||'''                     
                                                              AND TIPOCOMPROBANTE = ''' || UN_TIPO ||'''                    
                                                              AND ANO = ' || UN_ANO ||'', 'CONSECUTIVO', '0');
        IF (MI_CONSECUTIVO=0 ) THEN
          MI_RTA         := PCK_DATOS.FC_ACME('CONSECUTIVOTCP' ,'I' ,'COMPANIA,TIPOCOMPROBANTE,ANO,CONSECUTIVO, DATE_CREATED' ,''''|| UN_COMPANIA ||'''                                  
                                                ,''' || UN_TIPO ||'''                                  
                                                ,' || UN_ANO ||'                                  
                                                ,' || 1 ||', SYSDATE' ,NULL,NULL,NULL,NULL,NULL,NULL);
          MI_CONSECUTIVO := TO_NUMBER(TO_CHAR(UN_ANO) || LPAD(TO_CHAR(1), 6, 0));
        ELSE
          MI_CONSECUTIVO := TO_NUMBER(TO_CHAR(UN_ANO) || LPAD(TO_CHAR(MI_CONSECUTIVO), 6, 0));
        END IF;        
      END IF;
    END IF;
    RETURN MI_CONSECUTIVO;
  END FC_ENUMERAR;
  FUNCTION FC_TERCEROREGEMBARGO
    /*
    NAME              : FC_TERCEROREGEMBARGO
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : JUAN CAMILO RODRIGUEZ DIAZ
    DATE MIGRADOR     : 26/05/2017
