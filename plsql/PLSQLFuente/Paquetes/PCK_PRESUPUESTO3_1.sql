    @METHOD:  POST
    */
        UN_COMPANIA IN PCK_SUBTIPOS.TI_COMPANIA,
        UN_ANO      IN PCK_SUBTIPOS.TI_ANIO 
  )

  AS
    MI_CAMPOS PCK_SUBTIPOS.TI_CAMPOS;
    MI_CONDICION PCK_SUBTIPOS.TI_CONDICION;
    MI_TABLA PCK_SUBTIPOS.TI_TABLA;
    MI_RTA PCK_SUBTIPOS.TI_RTA_ACME;
    MI_MSGERROR PCK_SUBTIPOS.TI_CLAVEVALOR;
  BEGIN
    FOR MI_RS IN
    (
        SELECT D.COMPANIA,
                D.ANO,
                D.TIPO_CPTE,
                D.COMPROBANTE,
                D.CONSECUTIVO,
                D.CUENTA,
                D.CENTRO_COSTO,
                D.AUXILIAR,
                D.FUENTE_RECURSO,
                D.VALOR_DEBITO,
                D.VALOR_CREDITO,
                MODIFICACION_DEBITO,
                MODIFICACION_CREDITO,
                Y.MDEBITOS,
                Y.MCREDITOS
        FROM DETALLE_COMPROBANTE_PPTAL D
          INNER JOIN
            (
              SELECT  X.COMPANIA,
                      X.ANO_AFECT ANO,
                      X.TIPO_CPTE_AFECT TIPO_CPTE,
                      X.CMPTE_AFECTADO COMPROBANTE,
                      X.CONSECUTIVOPPTO CONSECUTIVO,
                      SUM( CASE
                            WHEN C.AFECTACION ='A' OR C.AFECTACION   ='R'
                              THEN VALOR_DEBITO
                            ELSE 0
                          END) MDEBITOS,
                      SUM( CASE
                            WHEN C.AFECTACION ='A' OR C.AFECTACION   ='R'
                              THEN VALOR_CREDITO
                            ELSE 0
                          END) MCREDITOS
                      FROM DETALLE_COMPROBANTE_PPTAL X
                      INNER JOIN TIPO_COMPROBPP T
                        ON X.COMPANIA  =T.COMPANIA
                        AND X.TIPO_CPTE=T.CODIGO
                      INNER JOIN CLASECNTPRES C
                        ON (T.CLASE              =C.CODIGO)
                      WHERE X.TIPO_CPTE_AFECT IS NOT NULL
                        AND X.CMPTE_AFECTADO    IS NOT NULL
                      GROUP BY  X.COMPANIA,
                                X.ANO_AFECT,
                                X.TIPO_CPTE_AFECT,
                                X.CMPTE_AFECTADO,
                                X.CONSECUTIVOPPTO
            ) Y 
            ON D.COMPANIA       = Y.COMPANIA
              AND D.ANO         = Y.ANO
              AND D.TIPO_CPTE   = Y.TIPO_CPTE
              AND D.COMPROBANTE = Y.COMPROBANTE
              AND D.CONSECUTIVO = Y.CONSECUTIVO
            WHERE D.COMPANIA  = UN_COMPANIA
              AND D.ANO         = UN_ANO
              AND ( MODIFICACION_DEBITO <> MDEBITOS OR MODIFICACION_CREDITO  <> MCREDITOS )
    )
    LOOP
      BEGIN
        BEGIN
          MI_CAMPOS    := 'MODIFICACION_DEBITO = '||MI_RS.MDEBITOS||',                    
                          MODIFICACION_CREDITO = '||MI_RS.MCREDITOS||'';

          MI_TABLA     := 'DETALLE_COMPROBANTE_PPTAL';

          MI_CONDICION := 'COMPANIA       = '''||MI_RS.COMPANIA||'''     
                          AND ANO         = '|| MI_RS.ANO ||'     
                          AND TIPO_CPTE   = '''||MI_RS.TIPO_CPTE||'''     
                          AND COMPROBANTE = '||MI_RS.COMPROBANTE ||'     
                          AND CONSECUTIVO = '||MI_RS.CONSECUTIVO||'';

          MI_RTA       := PCK_DATOS.FC_ACME(UN_ACCION => 'M', UN_TABLA => MI_TABLA, UN_CAMPOS => MI_CAMPOS, UN_CONDICION => MI_CONDICION);

        EXCEPTION
