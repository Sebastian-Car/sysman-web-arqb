  PROCEDURE PR_COMPROBANTEACOPIAR
    /*
    NAME              : PR_COMPROBANTEACOPIAR
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : JAVIER VILLATE
    DATE MIGRADOR     : 26/05/2017
    TIME              : 13:04 PM
    SOURCE MODULE     : Presupuesto
    MODIFIER          :
    DATE MODIFIED     :
    TIME              :
    DESCRIPTION       : Procedimiento que permite seleccionar y copiar los detalles de un comprobante a uno nuevo
    : Se ejecuta desde el botón copiar de del comprobante presupuestal.
    @NAME:  comprobanteAcopiar
    @METHOD:  GET
    */
    (
      UN_COMPANIA     IN PCK_SUBTIPOS.TI_COMPANIA,
      UN_ANO          IN PCK_SUBTIPOS.TI_ANIO,
      UN_TIPO         IN PCK_SUBTIPOS.TI_TIPOCOMPROBANTEPPTAL,
      UN_NUMERO       IN PCK_SUBTIPOS.TI_NUMEROCOMPROBANTEPPTAL,
      UN_TERCERO      IN PCK_SUBTIPOS.TI_TERCERO,
      UN_SUCURSAL     IN PCK_SUBTIPOS.TI_SUCURSAL,
      UN_FECHA        IN DATE,
      UN_NUMEROCOPIAR IN PCK_SUBTIPOS.TI_NUMEROCOMPROBANTEPPTAL,
      UN_USUARIO      IN VARCHAR2 )
  AS
    MI_CONSECUTIVO NUMBER := 0;
    MI_RTA         VARCHAR2(32000);
    MI_EXISTE PCK_SUBTIPOS.TI_TEXTO1 DEFAULT 'N';
    MI_TABLA PCK_SUBTIPOS.TI_TABLA;
    MI_CAMPOS PCK_SUBTIPOS.TI_CAMPOS;
    MI_VALORES PCK_SUBTIPOS.TI_VALORES;
    MI_DESCRIPCION DETALLE_COMPROBANTE_PPTAL.DESCRIPCION%TYPE;
    MI_TEXTO COMPROBANTE_PPTAL.TEXTO%TYPE;
    MI_NRO_DOCUMENTO DETALLE_COMPROBANTE_PPTAL.NRO_DOCUMENTO%TYPE;
    MI_VLR_DOCUMENTO PCK_SUBTIPOS.TI_DOBLE;
    MI_REFERENCIA DETALLE_COMPROBANTE_PPTAL.REFERENCIA%TYPE;
    MI_TIPO_DOCUMENTO PCK_SUBTIPOS.TI_TIPO_DOCUMENTO;
    MI_FECHA_VCN_DOC DATE;
    MI_DESTINO COMPROBANTE_PPTAL.DESTINO%TYPE;
    MI_DEPENDENCIA COMPROBANTE_PPTAL.DEPENDENCIA%TYPE;
    MI_CARGO COMPROBANTE_PPTAL.CARGO%TYPE;
    MI_CONDICION PCK_SUBTIPOS.TI_CONDICION;
    MI_RTA_ACME NUMBER;
    MI_TERCERO DETALLE_COMPROBANTE_PPTAL.TERCERO%TYPE;
    MI_SUCURSAL DETALLE_COMPROBANTE_PPTAL.SUCURSAL%TYPE;
    MI_PCKDATOS PCK_SUBTIPOS.TI_ENTERO;
  BEGIN
    BEGIN
      --Verifica si el comprobante nuevo ya tiene detalles y si ya tiene informa y no deja seguir.
      SELECT DISTINCT 'X'
      INTO MI_EXISTE
      FROM DETALLE_COMPROBANTE_PPTAL
      WHERE COMPANIA =UN_COMPANIA
      AND ANO        =UN_ANO
      AND TIPO_CPTE  =UN_TIPO
      AND COMPROBANTE=UN_NUMERO;
    EXCEPTION
    WHEN NO_DATA_FOUND THEN
      MI_EXISTE := 'N';
    END;
    IF MI_EXISTE<>'N' THEN
      BEGIN
        RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;
      EXCEPTION
      WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN
        PCK_ERR_MSG.RAISE_WITH_MSG( UN_EXC_COD => SQLCODE, UN_ERROR_COD => PCK_ERRORES.ERR_PPTO_COPIARDE );
      END;
    END IF;
    --selecciona los datos del comprobante del cual se va a copiar y los actualiza en el nuevo comprobante
    SELECT DESCRIPCION,
      TEXTO,
      NRO_DOCUMENTO,
      VLR_DOCUMENTO,
      REFERENCIA,
      TIPO_DOCUMENTO,
      FECHA_VCN_DOC,
      DESTINO,
      DEPENDENCIA,
      CARGO
    INTO MI_DESCRIPCION,
      MI_TEXTO,
      MI_NRO_DOCUMENTO,
      MI_VLR_DOCUMENTO,
      MI_REFERENCIA,
      MI_TIPO_DOCUMENTO,
      MI_FECHA_VCN_DOC,
      MI_DESTINO,
      MI_DEPENDENCIA,
      MI_CARGO
    FROM COMPROBANTE_PPTAL
    WHERE COMPANIA=UN_COMPANIA
    AND ANO       =UN_ANO
    AND TIPO      =UN_TIPO
    AND NUMERO    =UN_NUMEROCOPIAR;
    MI_TABLA     := 'COMPROBANTE_PPTAL';
    MI_CAMPOS    := ' DESCRIPCION   = '''||MI_DESCRIPCION ||'''                    
,TEXTO         ='''||MI_TEXTO||'''                    
,NRO_DOCUMENTO ='''||MI_NRO_DOCUMENTO||'''                    
,VLR_DOCUMENTO ='||MI_VLR_DOCUMENTO||'                    
,REFERENCIA    ='''||MI_REFERENCIA||'''                    
,TIPO_DOCUMENTO='''||MI_TIPO_DOCUMENTO||'''                    
,FECHA_VCN_DOC =TO_DATE('''||MI_FECHA_VCN_DOC||''', ''DD/MM/YYYY HH24:MI:SS'')                    
,DESTINO       ='''||MI_DESTINO||'''                    
,DEPENDENCIA   ='''||MI_DEPENDENCIA||'''                    
,CARGO         ='''||MI_CARGO||'''                    
,DATE_MODIFIED = SYSDATE                    
,MODIFIED_BY   = '''||UN_USUARIO||''' ';
    MI_CONDICION := '   COMPANIA = '''||UN_COMPANIA ||'''                    
AND ANO      = '||UN_ANO||'                     
AND TIPO     = '''||UN_TIPO||'''                    
AND NUMERO   = '||UN_NUMERO;
    MI_RTA_ACME  := PCK_DATOS.FC_ACME(UN_TABLA => MI_TABLA, UN_ACCION => 'M', UN_CAMPOS => MI_CAMPOS, UN_CONDICION => MI_CONDICION);
    --Selecciona todos los detalles del comprobante a copiar para insertarlos en el nuevo
    BEGIN
      FOR RS IN
      (SELECT CONSECUTIVO,
        CUENTA,
        NATURALEZA,
        TIPO_DOCUMENTO,
        NRO_DOCUMENTO,
        CENTRO_COSTO,
        AUXILIAR,
        FUENTE_RECURSO,
        REFERENCIA,
        NVL(CONTRACTUAL,0)CONTRACTUAL,
        NVL(PAPELES,0)PAPELES,
        SECTOR,
        PROGRAMA,
        SUBPROGRAMA,
        COD_PROD_CUIPO,
        CODIGO_BPIN,
        CODIGO_CCPET,
        CODIGO_CPC,
        CODIGOUNIDADEJE,
        FUENTE_CUIPO,
        CODIGOCCPETREGA,
        POLITICA_PUBLICA,
        DETALLE_SECTORIAL,
        RECURSO_SGR
      FROM DETALLE_COMPROBANTE_PPTAL
      WHERE COMPANIA  =UN_COMPANIA
      AND ANO         =UN_ANO
      AND TIPO_CPTE   =UN_TIPO
      AND COMPROBANTE =UN_NUMEROCOPIAR
      ORDER BY COMPANIA,
        ANO,
        CONSECUTIVO
      )
      LOOP
        MI_CAMPOS  :='COMPANIA,     ANO,
TIPO_CPTE,     COMPROBANTE,
CONSECUTIVO,   CUENTA,
FECHA,         NATURALEZA,
DESCRIPCION,   VALOR_DEBITO,
VALOR_CREDITO, TIPO_DOCUMENTO,
NRO_DOCUMENTO, CENTRO_COSTO,
TERCERO,       SUCURSAL,
AUXILIAR,      FUENTE_RECURSO,
CONTRACTUAL,   PAPELES,
SECTOR,         PROGRAMA,
SUBPROGRAMA,    COD_PROD_CUIPO,
CODIGO_BPIN,    CODIGO_CCPET,
CODIGO_CPC,     CODIGOUNIDADEJE,
FUENTE_CUIPO,   CODIGOCCPETREGA,
POLITICA_PUBLICA,   DETALLE_SECTORIAL,
RECURSO_SGR,    PAC_DISPONIBLE, REFERENCIA,
CREATED_BY,    DATE_CREATED';
        MI_VALORES :=''''||UN_COMPANIA||''',
'||UN_ANO||',
'''||UN_TIPO||''',
'||UN_NUMERO||',
'||RS.CONSECUTIVO||',
'''||RS.CUENTA||''',
TO_DATE(''' || TO_CHAR(UN_FECHA, 'DD/MM/YYYY') || ''',''DD/MM/YYYY''),
'''||RS.NATURALEZA||''',
'''||MI_DESCRIPCION||''',
