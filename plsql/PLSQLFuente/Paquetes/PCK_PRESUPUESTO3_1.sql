    UN_SUCURSAL  =>sucursal
    UN_TIPOCOMPROBANTE=>Tipo de comprobante
    @NAME:  terceroRegistraEmbargo
    @METHOD:  GET
    */
    (
      UN_COMPANIA        IN PCK_SUBTIPOS.TI_COMPANIA,
      UN_MODULO          IN PCK_SUBTIPOS.TI_MODULO,
      UN_TERCERO         IN PCK_SUBTIPOS.TI_TERCERO,
      UN_SUCURSAL        IN PCK_SUBTIPOS.TI_SUCURSAL,
      UN_TIPOCOMPROBANTE IN PCK_SUBTIPOS.TI_TIPOCOMPROBANTE )
    RETURN PCK_SUBTIPOS.TI_LOGICO
  AS
    MI_PARAMETRO PCK_SUBTIPOS.TI_CAMPOS;
    MI_NOMBRE PCK_SUBTIPOS.TI_CAMPOS;
    MI_CAMPOS PCK_SUBTIPOS.TI_CAMPOS;
    MI_RPTA PCK_SUBTIPOS.TI_LOGICO;
    MI_CONTEO PCK_SUBTIPOS.TI_ENTERO;
    -- Reemplazos en manejo de errores
    MI_REEMPLAZOS PCK_SUBTIPOS.TI_CLAVEVALOR;
  BEGIN
    BEGIN
      IF UN_COMPANIA IS NULL OR UN_MODULO IS NULL OR UN_TERCERO IS NULL OR UN_SUCURSAL IS NULL OR UN_TIPOCOMPROBANTE IS NULL THEN
        MI_CAMPOS    :='COMPANIA='||UN_COMPANIA||','|| 'MODULO='||UN_MODULO||','|| 'TERCERO='||UN_TERCERO||','|| 'SUCURSAL='||UN_SUCURSAL||','|| 'TIPOCOMPROBANTE='||UN_TIPOCOMPROBANTE;
        RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;
      END IF;
    EXCEPTION
    WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN
      MI_REEMPLAZOS (0).CLAVE := 'PARAMETROS';
      MI_REEMPLAZOS (0).VALOR := MI_CAMPOS;
      PCK_ERR_MSG.RAISE_WITH_MSG( UN_EXC_COD => SQLCODE, UN_ERROR_COD => PCK_ERRORES.ERR_PPTO_TERREGEMB_VACCAMPOS, UN_REEMPLAZOS => MI_REEMPLAZOS );
    END;
    MI_CONTEO                                  :=0;
    MI_NOMBRE                                  :='MANEJA ALERTA DE EMBARGO EN COMPROBANTES';
    MI_PARAMETRO                               :=PCK_SYSMAN_UTL.FC_PAR(UN_COMPANIA =>UN_COMPANIA, UN_NOMBRE =>MI_NOMBRE, UN_MODULO =>UN_MODULO, UN_FECHA_PAR=>SYSDATE, UN_IND_MAYUS=>-1);
    MI_PARAMETRO                               := NVL(MI_PARAMETRO,'NO');
    IF MI_PARAMETRO                             ='SI' THEN
      MI_NOMBRE                                :='ALERTA DE EMBARGO EN COMPROBANTES';
      MI_PARAMETRO                             :=PCK_SYSMAN_UTL.FC_PAR(UN_COMPANIA =>UN_COMPANIA, UN_NOMBRE =>MI_NOMBRE, UN_MODULO =>UN_MODULO, UN_FECHA_PAR=>SYSDATE, UN_IND_MAYUS=>-1);
      IF INSTRB(MI_PARAMETRO,UN_TIPOCOMPROBANTE)>0 THEN
        SELECT COUNT(1)
        INTO MI_CONTEO
        FROM TERCERO
        WHERE COMPANIA   = UN_COMPANIA
        AND NIT          = UN_TERCERO
        AND SUCURSAL     = UN_SUCURSAL
        AND EMBARGO NOT IN (0);
      END IF;
    END IF;
    IF MI_CONTEO > 0 THEN
      MI_RPTA   :=1;
    ELSE
      MI_RPTA:=0;
    END IF;
    RETURN MI_RPTA;
  EXCEPTION
  WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN
    PCK_ERR_MSG.RAISE_WITH_MSG(UN_EXC_COD => SQLCODE, UN_ERROR_COD => PCK_ERRORES.ERR_PPTO_TERREGEMB_ERROTHER);
  END FC_TERCEROREGEMBARGO;
