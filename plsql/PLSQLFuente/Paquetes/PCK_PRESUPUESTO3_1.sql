FUNCTION FC_VALIDACIONES_AFECTADO(
 /*
      NAME              : FC_VALIDACIONES_AFECTADO
      AUTHOR            : LINA PAOLA VEGA AVELLA y MARIA CAMILA ROSERO PAZOS
      DATE              : 27/07/2023
      TIME              : 09:26 AM
      SOURCE MODULE     : Presupuesto
      TIME              :
      DESCRIPTION       :     
      0 = pasa las validaciones, saldos correctos
      5 = si la solicitud está afectada y el valor editado es menor al afectado
      6 = si la solicitud está afectada y se quiere eliminar el item.
      7 = si se trata de eliminar un registro en la reduccion y tiene afectados relacionados
      @NAME:  actualizarValorSolicitado
      @METHOD:  GET
      */
      
  UN_COMPANIA               IN PCK_SUBTIPOS.TI_COMPANIA,
  UN_VALOR_DIGITADO         IN PCK_SUBTIPOS.TI_DOBLE,
  UN_TIPO_SOLICITUD         IN D_SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD%TYPE,
  UN_ACCION                 IN VARCHAR2,
  UN_CODIGO                 IN PLAN_PRESUPUESTAL.CODIGO%TYPE,
  UN_ANO                    IN PLAN_PRESUPUESTAL.ANO%TYPE,
  UN_NRO_SDP                IN SOLICITUDDISPONIBILIDAD.NUMERO%TYPE

)
  RETURN PCK_SUBTIPOS.TI_ENTERO
  AS
    MI_EXISTE                   NUMBER :=0;
    MI_VALORS                   NUMBER :=0;
    MI_VALORP                   NUMBER :=0;
    MI_SALIDA                   PCK_SUBTIPOS.TI_ENTERO := 0;
    MI_MSGERROR                 PCK_SUBTIPOS.TI_CLAVEVALOR;
    MI_RTA                      PCK_SUBTIPOS.TI_RTA_ACME;   
    MI_CONSULTA                 PCK_SUBTIPOS.TI_CONSULTA;
    MI_AFECTADO                 PCK_SUBTIPOS.TI_LOGICO;

    
  BEGIN
     MI_CONSULTA := 'SELECT AFECTADO FROM SOLICITUDDISPONIBILIDAD WHERE NUMERO =' || UN_NRO_SDP ||' AND TIPO_SOLICITUD = '||UN_TIPO_SOLICITUD;
        EXECUTE IMMEDIATE MI_CONSULTA INTO MI_AFECTADO; 
        
 IF MI_AFECTADO = -1 AND UN_TIPO_SOLICITUD = 1 THEN
        
         SELECT COUNT(0) EXISTE,   NVL(SUM(VALOR),0) VALORS,  NVL(SUM(PPTAL.VALOR_DEBITO),0) VALORP 
             INTO  MI_EXISTE, MI_VALORS, MI_VALORP 
              FROM  D_SOLICITUDDISPONIBILIDAD   SOL
                INNER JOIN DETALLE_COMPROBANTE_PPTAL PPTAL ON
                PPTAL.COMPANIA = SOL.COMPANIA
                AND PPTAL.ANO = SOL.ANO
                AND PPTAL.CONSECUTIVO = SOL.CONSECUTIVO
                AND PPTAL.CUENTA= SOL.RUBRO
                AND PPTAL.NRO_DOCUMENTO= TO_CHAR (SOL.NUMERO)
                    WHERE SOL.COMPANIA = UN_COMPANIA               
                    AND SOL.ANO = UN_ANO                
                    AND SOL.NUMERO = UN_NRO_SDP                
                    AND SOL.RUBRO = UN_CODIGO ;

      BEGIN
         IF UN_ACCION = 'E' THEN
                 IF MI_EXISTE >0 THEN
                     MI_SALIDA := 6; 
                     ELSE
                      MI_SALIDA := 0; 
                  END IF;

           ELSIF UN_ACCION = 'M' THEN
                 IF  MI_EXISTE >0 THEN
                    IF   UN_VALOR_DIGITADO < MI_VALORP  THEN             
                         MI_SALIDA := 5; 
                      ELSE 
                         MI_SALIDA := 0; 
                    END IF;
                  ELSE
                      MI_SALIDA := 0;   
                 END IF; 
          END IF;
    END;
   ELSIF  UN_TIPO_SOLICITUD = 3 THEN
   
        SELECT COUNT(0) EXISTE, NVL(SUM(SOL.VALOR),0) VALORS,  NVL(SUM(PPTAL.VALOR_CREDITO),0) MI_VALORP
        INTO  MI_EXISTE, MI_VALORS, MI_VALORP 
            FROM  DETALLE_COMPROBANTE_PPTAL PPTAL
            INNER JOIN SOLICITUDDISPONIBILIDAD SOL
                ON PPTAL.COMPANIA = SOL.COMPANIA
                AND PPTAL.ANO = SOL.ANO
                AND TO_CHAR(PPTAL.CMPTE_AFECTADO)= SOL.DISPONIBILIDAD_RELACIONADA
            INNER JOIN D_SOLICITUDDISPONIBILIDAD D_SOL 
               ON SOL.COMPANIA = D_SOL.COMPANIA
               AND SOL.NUMERO = D_SOL.NUMERO
               AND SOL.ANO = D_SOL.ANO 
             LEFT JOIN TIPO_COMPROBPP 
                ON   PPTAL.COMPANIA     = TIPO_COMPROBPP.COMPANIA
                AND   PPTAL.TIPO_CPTE    = TIPO_COMPROBPP.CODIGO 
                WHERE SOL.COMPANIA = UN_COMPANIA               
                    AND SOL.ANO = UN_ANO                
                    AND SOL.NUMERO = UN_NRO_SDP 
                    AND TIPO_COMPROBPP.CLASE = 'DMD'
                    AND D_SOL.RUBRO = UN_CODIGO ;
                    
                    BEGIN
         IF UN_ACCION = 'E' THEN
                 IF MI_EXISTE >0 THEN
                     MI_SALIDA := 7; 
                     ELSE
                      MI_SALIDA := 0; 
                  END IF;
                      
           ELSIF UN_ACCION = 'M' THEN
                 IF  MI_EXISTE >0 THEN
                    IF   UN_VALOR_DIGITADO > MI_VALORP  THEN             
                         MI_SALIDA := 8; 
                         ELSE 
                         MI_SALIDA := 0; 
                    END IF;
                  ELSE
                      MI_SALIDA := 0;   
                 END IF; 
          END IF;
    END;
   
 ELSE
    MI_SALIDA := 0;
END IF;

RETURN MI_SALIDA;
END FC_VALIDACIONES_AFECTADO;
