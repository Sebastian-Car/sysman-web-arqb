  PROCEDURE PR_REVISAR_AFECTACIONES_PPTO
  (
  /*
    NAME              : PR_REVISAR_AFECTACIONES_PPTO
    AUTHORS           : HENRY PUERTO V
    AUTHOR MIGRACION  : JONATHAN MALAVER JIMÉNEZ
    DATE MIGRADOR     : 27/06/2018                                                                                              
    TIME              :
