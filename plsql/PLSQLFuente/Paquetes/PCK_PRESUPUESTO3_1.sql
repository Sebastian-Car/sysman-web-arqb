      UN_ANO PCK_SUBTIPOS.TI_ANIO,
      UN_CODIGO PCK_SUBTIPOS.TI_CODIGOPPTAL )
  AS
    MI_EXISTE PCK_SUBTIPOS.TI_TEXTO1 DEFAULT 'N';
    MI_MSGERROR PCK_SUBTIPOS.TI_CLAVEVALOR;
  BEGIN
    BEGIN
      SELECT DISTINCT 'X'
      INTO MI_EXISTE
      FROM PLAN_PRESUPUESTAL INI
      WHERE COMPANIA =UN_COMPANIA
      AND ANO        = UN_ANO
      AND UN_CODIGO  = SUBSTR(CODIGO,1,LENGTH(UN_CODIGO))
      AND UN_CODIGO <> CODIGO;
    EXCEPTION
    WHEN NO_DATA_FOUND THEN
      MI_EXISTE:='N';
    END;
    IF MI_EXISTE<>'N' THEN
      BEGIN
        RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;
      EXCEPTION
      WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN
        MI_MSGERROR(1).CLAVE := 'CODIGO';
        MI_MSGERROR(1).VALOR := UN_CODIGO;
        MI_MSGERROR(2).CLAVE := 'ANO';
        MI_MSGERROR(2).VALOR := UN_ANO;
        PCK_ERR_MSG.RAISE_WITH_MSG( UN_EXC_COD => SQLCODE ,UN_TABLAERROR => 'PLAN_PRESUPUESTAL' ,UN_ERROR_COD => PCK_ERRORES.ERR_PRESU_ELIMINACUENTA_PPTAL ,UN_REEMPLAZOS => MI_MSGERROR );
      END;
    END IF;
  END PR_ELIMINAR_CUENTA_PPTAL;
