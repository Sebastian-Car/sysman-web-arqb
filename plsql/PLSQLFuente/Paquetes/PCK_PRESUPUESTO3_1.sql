  MI_EXISTE                 PCK_SUBTIPOS.TI_ENTERO;
  MI_ANO                    PCK_SUBTIPOS.TI_ANIO;
  MI_TIPO                   PCK_SUBTIPOS.TI_TIPOCOMPROBANTEPPTAL;
  MI_COMPROBANTE            DETALLE_COMPROBANTE_PPTAL.COMPROBANTE%TYPE;
  MI_TIPODOC                 BPNOVEDADPROYECTO.TIPOT%TYPE;
  MI_TOTAL                  PCK_SUBTIPOS.TI_ENTERO;
  MI_CLASECOMMPROBANTE      TIPO_COMPROBPP.CLASE%TYPE;
  MI_TABLA                  PCK_SUBTIPOS.TI_TABLA;
  MI_CAMPOS                 PCK_SUBTIPOS.TI_CAMPOS;
  MI_CONDICION              PCK_SUBTIPOS.TI_CONDICION;

 
BEGIN
    IF  UN_TIPO_CPTE  IS NULL  OR UN_COMPROBANTE IS NULL OR  UN_NUMEROSOLICITUD IS NULL THEN
    RETURN;
  END IF;
  BEGIN
    SELECT CLASE
    INTO MI_CLASECOMMPROBANTE
    FROM TIPO_COMPROBPP
    WHERE COMPANIA = UN_COMPANIA
      AND CODIGO = UN_TIPO_CPTE;   
    EXCEPTION
     WHEN NO_DATA_FOUND THEN
      MI_CLASECOMMPROBANTE := '';
    END;
    
 BEGIN
    SELECT  TIPO_DOCUMENTO
    INTO MI_TIPODOC
    FROM COMPROBANTE_PPTAL
    WHERE COMPANIA = UN_COMPANIA
    AND ANO = UN_ANO
    AND TIPO= UN_TIPO_CPTE
    AND NUMERO = UN_COMPROBANTE;   
    EXCEPTION
    WHEN NO_DATA_FOUND THEN
      MI_TIPODOC := '';
    END;
    
    IF  MI_CLASECOMMPROBANTE IN ('DIS') THEN
               
                  MI_CAMPOS:= ' AFECTADO = 0 ';
                  MI_ANO         := UN_ANO;
                  MI_TIPO        := UN_TIPO_CPTE;
                  MI_COMPROBANTE := UN_COMPROBANTE;
    END IF;
    IF  MI_TIPODOC = 'SCD' THEN
        MI_TABLA:= 'BPNOVEDADPROYECTO';
        MI_CONDICION:=' COMPANIA    = '''|| UN_COMPANIA ||'''
                        AND TIPOT = '''|| MI_TIPODOC||'''
                        AND CODIGO      = '|| UN_NUMEROSOLICITUD||'';
        BEGIN
            BEGIN
                PCK_DATOS.GL_RTA:=PCK_DATOS.FC_ACME(
                                    UN_TABLA     => MI_TABLA
                                    ,UN_ACCION    => 'M'
                                    ,UN_CAMPOS    => MI_CAMPOS
                                    ,UN_CONDICION => MI_CONDICION
                                         ) ;
                EXCEPTION WHEN PCK_EXCEPCIONES.EXC_ACTUALIZAR THEN
                    RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;
                END;
         EXCEPTION WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN
            PCK_ERR_MSG.RAISE_WITH_MSG(
                        UN_EXC_COD    => SQLCODE
                        ,UN_ERROR_COD  => PCK_ERRORES.ERR_BANCO_ACTCHECKPRES
                        ,UN_TABLAERROR => MI_TABLA
                         );
           END;
           
  END IF;  
END PR_VALIDARSOLICITUDCDP;  

FUNCTION FC_VAL_APROPIACION(
      /*
      NAME              : FC_VAL_APROPIACION
      AUTHOR            : SYSMAN, JM CC 4401
      SOURCE MODULE     : Presupuesto     :
      DESCRIPTION       : valida la apropiacion definitiva del rubro 
      --NAME:  validarAprociacion
      --METHOD:  GET
      */
    UN_COMPANIA               IN PCK_SUBTIPOS.TI_COMPANIA,
    UN_CODIGO                 IN PLAN_PRESUPUESTAL.CODIGO%TYPE,
    UN_ANO                    IN PLAN_PRESUPUESTAL.ANO%TYPE
      )
    RETURN PCK_SUBTIPOS.TI_ENTERO
  AS
    MI_SALIDA PCK_SUBTIPOS.TI_ENTERO := 0;
  BEGIN
    
    BEGIN 
        SELECT CASE WHEN NVL(RESUMENPPTO_A.APRDEFINITIVA,0) <> 0 THEN -1 ELSE 0 END INTO MI_SALIDA
            FROM (
                SELECT
                    V_SALDO_PLAN_PPTAL.COMPANIA,
                    V_SALDO_PLAN_PPTAL.ANO,
                    V_SALDO_PLAN_PPTAL.ID,
                    SUM(CASE
                            WHEN V_PLAN_PRESUPUESTAL.NATURALEZA = 'D'
                            THEN APROPIACION_DEBITO - APROPIACION_CREDITO
                            ELSE APROPIACION_CREDITO - APROPIACION_DEBITO
                        END)
                    + SUM(V_SALDO_PLAN_PPTAL.ADICION)
                    + SUM(V_SALDO_PLAN_PPTAL.REDUCCION)
                    + SUM(CASE
                            WHEN V_PLAN_PRESUPUESTAL.NATURALEZA = 'D'
                            THEN APLAZAM_DEBITO - APLAZAM_CREDITO
                            ELSE APLAZAM_CREDITO - APLAZAM_DEBITO
                        END)
                    + SUM(CASE
                            WHEN V_PLAN_PRESUPUESTAL.NATURALEZA = 'D'
                            THEN TRASLADO_DEBITO - TRASLADO_CREDITO
                            ELSE TRASLADO_CREDITO - TRASLADO_DEBITO
                        END) AS APRDEFINITIVA
                FROM V_PLAN_PRESUPUESTAL
                LEFT JOIN V_SALDO_PLAN_PPTAL
                    ON V_PLAN_PRESUPUESTAL.COMPANIA = V_SALDO_PLAN_PPTAL.COMPANIA
                   AND V_PLAN_PRESUPUESTAL.ANO = V_SALDO_PLAN_PPTAL.ANO
                   AND V_PLAN_PRESUPUESTAL.ID = V_SALDO_PLAN_PPTAL.ID
                WHERE V_SALDO_PLAN_PPTAL.COMPANIA = UN_COMPANIA
                  AND V_SALDO_PLAN_PPTAL.ANO = UN_ANO
                  AND V_SALDO_PLAN_PPTAL.ID = UN_CODIGO
                GROUP BY
                    V_SALDO_PLAN_PPTAL.COMPANIA,
                    V_SALDO_PLAN_PPTAL.ANO,
                    V_SALDO_PLAN_PPTAL.ID,
                    V_PLAN_PRESUPUESTAL.NATURALEZA
            ) RESUMENPPTO_A;

        EXCEPTION WHEN OTHERS THEN
            MI_SALIDA := 0;
        END; 
    
       RETURN MI_SALIDA;

END FC_VAL_APROPIACION;

