  PROCEDURE PR_ELIMINARCUENTAPPTAL(
      /*
      NAME              : PR_ELIMINARCUENTAPPTAL
      AUTHORS           : SYSMAN SAS
      AUTHOR MIGRACION  : YESIKA PAOLA BECERRA CASTRO
      DATE MIGRADOR     : 05/04/2018
      TIME              : 03:15 PM
      SOURCE MODULE     : PRESUPUESTO
      MODIFIER          :
      DATE MODIFIED     :
      TIME              :
      MODIFICATIONS     :
      DESCRIPTION       : Procedimiento que actualiza elimina cuenta presupuestal en las tablas PLAN_PPTAL_CONFIG,SALDO_AUX_PPTAL,SALDO_PLAN_PPTAL
      PARAMETERS        : UN_COMPANIA    => Compania por la cual ingresa a la aplicacion
      UN_ANIO         => ano seleccionado al ingresa en el plan presupuestal
      UN_CODIGO     => codigo seleccionado al eliminar
      @NAME:    eliminarCuentaPresupuestal
      @METHOD:  DELETE
      */
      UN_COMPANIA IN PCK_SUBTIPOS.TI_COMPANIA,
      UN_ANO      IN PCK_SUBTIPOS.TI_ANIO,
      UN_CODIGO   IN PCK_SUBTIPOS.TI_CODIGOPPTAL )
  AS
    MI_TABLA PCK_SUBTIPOS.TI_TABLA;
    MI_CONDICION PCK_SUBTIPOS.TI_CONDICION;
    MI_REEMPLAZOS PCK_SUBTIPOS.TI_CLAVEVALOR;
    MI_CUENTA PCK_SUBTIPOS.TI_CODIGOPPTAL;
  BEGIN
    BEGIN
      SELECT COUNT('X') CUENTA
      INTO MI_CUENTA
      FROM APROPIACIONESINICIALES
      WHERE APROPIACIONESINICIALES.COMPANIA = UN_COMPANIA
      AND APROPIACIONESINICIALES.ANO        = UN_ANO
      AND APROPIACIONESINICIALES.CODIGO     = UN_CODIGO;
    EXCEPTION
    WHEN NO_DATA_FOUND THEN
      MI_CUENTA := 0;
    END;
    BEGIN
      BEGIN
        IF MI_CUENTA <> 0 THEN
          RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;
        END IF;
      END;
    EXCEPTION
    WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN
      PCK_ERR_MSG.RAISE_WITH_MSG( UN_EXC_COD => SQLCODE, UN_ERROR_COD => PCK_ERRORES.ERR_PPTO_CUENTA_APROP, UN_TABLAERROR => 'APROPIACIONESINICIALES' );
    END;
    BEGIN
      SELECT COUNT('X') CUENTA
      INTO MI_CUENTA
      FROM DETALLE_COMPROBANTE_PPTAL
      WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA = UN_COMPANIA
      AND DETALLE_COMPROBANTE_PPTAL.ANO        = UN_ANO
      AND DETALLE_COMPROBANTE_PPTAL.CUENTA     = UN_CODIGO ;
    EXCEPTION
    WHEN NO_DATA_FOUND THEN
      MI_CUENTA := 0;
    END;
    BEGIN
      BEGIN
        IF MI_CUENTA <> 0 THEN
          RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;
        END IF;
      END;
    EXCEPTION
    WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN
      PCK_ERR_MSG.RAISE_WITH_MSG( UN_EXC_COD => SQLCODE, UN_ERROR_COD => PCK_ERRORES.ERR_PPTO_CUENTA_DET, UN_TABLAERROR => 'DETALLE_COMPROBANTE_PPTAL' );
    END;
    BEGIN
      SELECT COUNT('X') CUENTA
      INTO MI_CUENTA
      FROM FONDO
      WHERE COMPANIA          = UN_COMPANIA
      AND ANO                 = UN_ANO
      AND RUBRO_SIIF_EMPLEADO = UN_CODIGO
      OR RUBRO_SIIF_PATRONO   = UN_CODIGO;
    EXCEPTION
    WHEN NO_DATA_FOUND THEN
      MI_CUENTA := 0;
    END;
    BEGIN
      BEGIN
        IF MI_CUENTA <> 0 THEN
          RAISE PCK_EXCEPCIONES.EXC_PRESUPUESTO;
        END IF;
      END;
    EXCEPTION
    WHEN PCK_EXCEPCIONES.EXC_PRESUPUESTO THEN
      PCK_ERR_MSG.RAISE_WITH_MSG( UN_EXC_COD => SQLCODE, UN_ERROR_COD => PCK_ERRORES.ERR_PPTO_CUENTA_FONDO, UN_TABLAERROR => 'FONDO' );
    END;
    BEGIN
      SELECT COUNT('X') CUENTA
      INTO MI_CUENTA
      FROM CONCEPTOS
      WHERE COMPANIA    = UN_COMPANIA
