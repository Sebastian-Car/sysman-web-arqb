    MI_DESCRIPCION COMPROBANTE_PPTAL.DESCRIPCION%TYPE;   -- Variable que almacenara la descripcion del comprobante
    MI_FECHAVENCIMIENTO DATE;                            -- Variable que almacenara la fecha de vencimiento del comprobante a liberar
    MI_COLUMNA          VARCHAR2(4 CHAR);                -- Variable que almacenara la naturaleza interes consulta retornada en la consulta
    MI_CLASE PCK_SUBTIPOS.TI_TIPOCOMPROBANTEPPTAL;       -- Variable que almacenara la clase de comprobante a liberar
