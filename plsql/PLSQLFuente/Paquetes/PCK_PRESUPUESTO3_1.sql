  FUNCTION FC_CALCULARSALDONETO
    /*
    NAME              : FC_CALCULARSALDONETO
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : JAVIER VILLATE
    DATE MIGRADOR     : 26/05/2017
    TIME              : 13:04 PM
    SOURCE MODULE     : Presupuesto
    MODIFIER          :
    DATE MODIFIED     :
    TIME              :
    DESCRIPTION       : Devuelve el saldo neto de una cuenta a partir del valor
    débito y crédito recibido por parámetro.
    @NAME:  calcularSaldoNeto
    @METHOD:  GET
    */
    (
      UN_COMPANIA PCK_SUBTIPOS.TI_COMPANIA,
      UN_ANO PCK_SUBTIPOS.TI_ANIO,
      UN_TIPO PCK_SUBTIPOS.TI_TIPOCOMPROBANTEPPTAL,
      UN_COMPROBANTE PCK_SUBTIPOS.TI_NUMEROCOMPROBANTEPPTAL,
      UN_CONSECUTIVO PCK_SUBTIPOS.TI_CONSECUTIVOPPTAL,
      UN_CUENTA PCK_SUBTIPOS.TI_CODIGOPPTAL,
      UN_VALOR_DEBITO PCK_SUBTIPOS.TI_DOBLE,
      UN_VALOR_CREDITO PCK_SUBTIPOS.TI_DOBLE )
    RETURN PCK_SUBTIPOS.TI_DOBLE
  AS
    MI_SALDONETO PCK_SUBTIPOS.TI_DOBLE;
  BEGIN
    BEGIN
      SELECT
        CASE
          WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
          THEN (UN_VALOR_DEBITO -UN_VALOR_CREDITO)-(DEBITO_AFECTADO-CREDITO_AFECTADO)+(MODIFICACION_DEBITO-MODIFICACION_CREDITO)
          ELSE (UN_VALOR_CREDITO-UN_VALOR_DEBITO)-(CREDITO_AFECTADO-DEBITO_AFECTADO)+(MODIFICACION_CREDITO-MODIFICACION_DEBITO)
        END SALDONETO
      INTO MI_SALDONETO
      FROM PLAN_PRESUPUESTAL
      INNER JOIN DETALLE_COMPROBANTE_PPTAL
      ON PLAN_PRESUPUESTAL.COMPANIA            =DETALLE_COMPROBANTE_PPTAL.COMPANIA
      AND PLAN_PRESUPUESTAL.ANO                =DETALLE_COMPROBANTE_PPTAL.ANO
      AND PLAN_PRESUPUESTAL.CODIGO             =DETALLE_COMPROBANTE_PPTAL.CUENTA
      WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA =UN_COMPANIA
      AND DETALLE_COMPROBANTE_PPTAL.ANO        =UN_ANO
      AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE  =UN_TIPO
      AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE=UN_COMPROBANTE
      AND DETALLE_COMPROBANTE_PPTAL.CONSECUTIVO=UN_CONSECUTIVO
      AND DETALLE_COMPROBANTE_PPTAL.CUENTA     =UN_CUENTA;
    EXCEPTION
    WHEN NO_DATA_FOUND THEN
      MI_SALDONETO:=0;
      RETURN MI_SALDONETO;
    END;
    RETURN MI_SALDONETO;
  END FC_CALCULARSALDONETO;
