PROCEDURE PR_CARGARINDRESULTADO

(     
      UN_COMPANIA IN PCK_SUBTIPOS.TI_COMPANIA,
      UN_VIGENCIA IN PI_TOTAL_FUENTES_INDICADOR.VIGENCIA_PLAN%TYPE
)
AS    
      MI_INDPROCESO          PCK_SUBTIPOS.TI_CAMPOS;
      MI_INDPRODUCTO         PCK_SUBTIPOS.TI_CAMPOS;
      MI_CODPRODUCTO         PCK_SUBTIPOS.TI_CAMPOS;


      MI_TABLA                PCK_SUBTIPOS.TI_TABLA;
      MI_CAMPOS               PCK_SUBTIPOS.TI_CAMPOS;
      MI_CONDICION            PCK_SUBTIPOS.TI_CONDICION;
      MI_RTA                  PCK_SUBTIPOS.TI_RTA_ACME;

BEGIN
      FOR MI_RST IN 

      (
            SELECT       PI_RELACION_PROD_RTDO.ID_PLAN_PRODUCTO, 
                         PI_RELACION_PROD_RTDO.CODIGO_PRODUCTO, 
                         PI_RELACION_PROD_RTDO.ID_PLAN_RESULTADO,  
                         PI_RELACION_PROD_RTDO.CODIGO_RESULTADO, 
                         PI_INDICADOR.DESCRIPCION_META, 
                         PI_INDICADOR.NOMBRE, 
                         PI_INDICADOR.LB, 
                         PI_RELACION_PROD_RTDO.PRINCIPAL 
            FROM         PI_RELACION_PROD_RTDO 
            INNER JOIN   PI_INDICADOR 
            ON           (PI_RELACION_PROD_RTDO.COMPANIA = PI_INDICADOR.COMPANIA) 
            AND          (PI_RELACION_PROD_RTDO.ID_PLAN_RESULTADO = PI_INDICADOR.ID_PLAN) 
            AND          (PI_RELACION_PROD_RTDO.CODIGO_RESULTADO = PI_INDICADOR.CODIGO) 
            WHERE        PI_RELACION_PROD_RTDO.COMPANIA = '''||UN_COMPANIA||''' 
            AND          PI_RELACION_PROD_RTDO.VIGENCIA = '''||UN_VIGENCIA||'''
            ORDER BY     PI_RELACION_PROD_RTDO.ID_PLAN_PRODUCTO, 
                         PI_RELACION_PROD_RTDO.CODIGO_PRODUCTO, 
                         PI_RELACION_PROD_RTDO.PRINCIPAL
      )LOOP

            MI_INDPROCESO :='';
            MI_INDPRODUCTO :='';
            MI_CODPRODUCTO :='';


            IF MI_INDPRODUCTO <> MI_RST.ID_PLAN_PRODUCTO OR MI_CODPRODUCTO <> MI_RST.CODIGO_PRODUCTO THEN

                  IF MI_INDPROCESO <> '' THEN                   

                  MI_RTA := PCK_DATOS.FC_ACME(
                          UN_TABLA          => MI_TABLA,
                          UN_ACCION         => 'M',
                          UN_CONDICION      => MI_CONDICION,
                          UN_CAMPOS         => MI_CAMPOS

                          );
                  END IF;

                  MI_INDPRODUCTO := MI_RST.ID_PLAN_PRODUCTO;
                  MI_CODPRODUCTO := MI_RST.CODIGO_PRODUCTO;

                  ELSIF MI_INDPROCESO = '2' THEN 

                  CONTINUE;

            END IF;

       MI_TABLA := 'PI_INF_PLANINDICATIVO_DNP';

       MI_CONDICION := 'ID_METAPRODUCTO = '''|| MI_RST.ID_PLAN_PRODUCTO|| '''
                        AND COD_METAPRODUCTO = '''||MI_RST.CODIGO_PRODUCTO|| '''';


      IF MI_RST.PRINCIPAL IS NOT NULL AND MI_INDPROCESO <> '' THEN 

      MI_CAMPOS :=  'ID_METARESULTADOPRINC = '''||MI_RST.ID_PLAN_RESULTADO||'''
                          , COD_METARESULTADOPRINC = '''||MI_RST.CODIGO_RESULTADO||'''
                          , DESCRIPCION_METARESULTADOPRINC = '''||MI_RST.DESCRIPCION_META||'''
                          , INDICADOR_METARESULTADOPRINC = '''||MI_RST.NOMBRE||'''
                          , LB_METARESULTADOPRINC = '''||MI_RST.LB||'''';

            MI_INDPROCESO := 'IP';

      END IF;


      IF MI_INDPROCESO = 'P' THEN 

            MI_CAMPOS :=  'DESCRIPCION_METARESULTADO1 = '''||MI_RST.DESCRIPCION_META||'''';

            -- MI_INDPROCESO := "I1";

      ELSIF MI_INDPROCESO = '' THEN

            MI_CAMPOS :=  'DESCRIPCION_METARESULTADO1 = '''||MI_RST.DESCRIPCION_META||''' ';

            --MI_INDPROCESO := "I1";

      END IF;

      IF MI_INDPROCESO = '1' THEN

            MI_CAMPOS :=  'DESCRIPCION_METARESULTADO2 = '''||MI_RST.DESCRIPCION_META||'''';

            --MI_INDPROCESO := 'I2';

      END IF;

      MI_INDPROCESO := substr(MI_INDPROCESO , 2,1 );

      END LOOP;

      IF MI_CAMPOS <> '' THEN

            MI_RTA := PCK_DATOS.FC_ACME(
                                UN_TABLA          => MI_TABLA,
                                UN_ACCION         => 'M',
                                UN_CONDICION      => MI_CONDICION,
                                UN_CAMPOS         => MI_CAMPOS

                                );

      END IF;


END PR_CARGARINDRESULTADO;

