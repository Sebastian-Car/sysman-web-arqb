	BEGIN
		BEGIN 

			MI_TABLA :='PI_TOTAL_FUENTES_INDICADOR';

  		MI_CAMPOS := 'COMPANIA,
							ID_PLAN,
							VIGENCIA_PLAN,
							VIGENCIA_META,
							VALOR_PRESUPUESTO,
							VALOR_COMPROMETIDO,
							VALOR_PAGADO,
							CODIGO,
							CODIGO_DNP';

			MI_VALORES := '	SELECT 
							FUENTESBP.COMPANIA,
							FUENTESBP.ID_PLAN,
							FUENTESBP.VIGENCIA_PLAN,
							FUENTESBP.VIGENCIA_META,
							SUM(FUENTESBP.PRESUPUESTO  * INDICADOR.PARTICIPACION) AS VALOR_PRESUPUESTO,
							SUM(FUENTESBP.COMPROMETIDO * INDICADOR.PARTICIPACION) AS VALOR_COMPROMETIDO,
							SUM(FUENTESBP.PAGADO       * INDICADOR.PARTICIPACION) AS VALOR_PAGADO,
							INDICADOR.CODIGO,
							FUENTE.CODIGO_DNP
							FROM PI_INF_PLANINDICATIVO_DNP DNP
							INNER JOIN BP_PLAN_INDICATIVO_FUENTES FUENTESBP
							ON DNP.SUBPROGRAMA = FUENTESBP.ID_PLAN
							AND DNP.VIGENCIA   = FUENTESBP.VIGENCIA_PLAN
							AND DNP.COMPANIA   = FUENTESBP.COMPANIA
							INNER JOIN FUENTE_RECURSOS FUENTE
							ON FUENTESBP.VIGENCIA_META = FUENTE.ANO
							AND FUENTESBP.FUENTE       = FUENTE.CODIGO
							AND FUENTESBP.COMPANIA     = FUENTE.COMPANIA
							INNER JOIN PI_INDICADOR INDICADOR
							ON DNP.COD_METAPRODUCTO     = INDICADOR.CODIGO
							AND DNP.ID_METAPRODUCTO     = INDICADOR.ID_PLAN
							AND DNP.VIGENCIA            = INDICADOR.VIGENCIA_INICIAL
							AND DNP.COMPANIA            = INDICADOR.COMPANIA
							WHERE FUENTESBP.COMPANIA    = '''||UN_COMPANIA||'''
							AND FUENTESBP.VIGENCIA_PLAN ='''||UN_VIGENCIA_INICIAL||'''
							GROUP BY FUENTESBP.COMPANIA,
							FUENTESBP.ID_PLAN,
							FUENTESBP.VIGENCIA_PLAN,
							FUENTESBP.VIGENCIA_META,
							FUENTE.CODIGO_DNP,
							INDICADOR.CODIGO,
							FUENTE.CODIGO_DNP';


			MI_RTA	:= PCK_DATOS.FC_ACME(
					UN_TABLA	  =>	MI_TABLA,
					UN_ACCION	  =>	'IS',
					UN_CAMPOS	  =>	MI_CAMPOS,
					UN_VALORES	=>	MI_VALORES
										);

			EXCEPTION WHEN PCK_EXCEPCIONES.EXC_INSERTAR THEN
		          RAISE PCK_EXCEPCIONES.EXC_PLANDES;
	    	END;

	    EXCEPTION WHEN PCK_EXCEPCIONES.EXC_PLANDES THEN
        PCK_ERR_MSG.RAISE_WITH_MSG(
			UN_EXC_COD    	=>	SQLCODE,
			UN_ERROR_COD 	=>	PCK_ERRORES.ERR_PLANDES_INSERT_FUENTESIND
								  );
	END;	    
