FUNCTION FC_JSON_ORGANIGRAMA
  /*
    NAME              : FC_JSON_ORGANIGRAMA
    AUTHORS           : SYSMAN LTDA
    AUTHOR MIGRACION  : ELKIN GEOVANNY AMAYA SILVA
    DATE MIGRADOR     : 26/06/2019
    TIME              : 17:05 AM     
    MODIFIER          : 
    DATE MODIFIED     : 
    DESCRIPTION       : CREA EL ARCHIVO EN FORMATO JSON DEL ORGANIGRAMA PARA BMANGA
    MODIFICATIONS     : 
    @NAME:  jsonOrganigramaBmanga
*/
  (
    UN_COMPANIA   IN PCK_SUBTIPOS.TI_COMPANIA    
  ) RETURN CLOB AS  
   MI_SALIDA        CLOB := ''; 
   MI_CODIGO_PADRE  ORGANIGRAMA.CODIGO_PADRE%TYPE;
   MI_CANTNODOS     NUMBER := 0;
   MI_NOMBRECOMPANIA VARCHAR2(128):='';

  BEGIN


 SELECT COUNT(*)
 INTO 
 MI_CANTNODOS
 FROM ORGANIGRAMA 
 WHERE COMPANIA = UN_COMPANIA
   AND CODIGO_PADRE IN (SELECT MIN(CODIGO_PADRE) 
                        FROM ORGANIGRAMA 
                        WHERE COMPANIA = UN_COMPANIA);


   IF MI_CANTNODOS > 1 THEN
    BEGIN
        BEGIN
            RAISE PCK_EXCEPCIONES.EXC_NOMINA;
        END;         

        EXCEPTION WHEN PCK_EXCEPCIONES.EXC_NOMINA THEN

           PCK_ERR_MSG.RAISE_WITH_MSG(UN_EXC_COD    => SQLCODE,
                UN_ERROR_COD  => PCK_ERRORES.ERR_NODOREPETIDO  
                );
    END;        

   END IF;


      SELECT MIN(CODIGO_PADRE) 
      INTO MI_CODIGO_PADRE
      FROM ORGANIGRAMA 
      WHERE COMPANIA = UN_COMPANIA;


 
      SELECT NOMBRE
      INTO MI_NOMBRECOMPANIA
      FROM COMPANIA
      WHERE CODIGO = UN_COMPANIA;


  MI_SALIDA := '{'|| ' "id": 1,'||
               ' "title":"' || MI_NOMBRECOMPANIA || '", "root": ';


  <<RecorrerOrganigrama>>                 

     MI_SALIDA := MI_SALIDA|| FC_JSON_ORGANIGRAMARECU (UN_COMPANIA => UN_COMPANIA,
                                        UN_CODIGOPADRE => MI_CODIGO_PADRE);
    MI_SALIDA := MI_SALIDA||'}';

    RETURN MI_SALIDA;

END FC_JSON_ORGANIGRAMA;


