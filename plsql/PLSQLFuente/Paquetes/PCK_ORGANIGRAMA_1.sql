FUNCTION FC_JSON_ORGANIGRAMARECU
  /*
    NAME              : FC_JSON_ORGANIGRAMA
    AUTHORS           : SYSMAN LTDA
    AUTHOR MIGRACION  : ELKIN GEOVANNY AMAYA SILVA
    DATE MIGRADOR     : 26/06/2019
    TIME              : 17:05 AM     
    MODIFIER          : 
    DATE MODIFIED     : 
    DESCRIPTION       : CREA EL ARCHIVO EN FORMATO JSON DEL ORGANIGRAMA PARA BMANGA
    MODIFICATIONS     : 
    @NAME:  jsonOrgBmangaRecu
*/
  (
    UN_COMPANIA   IN PCK_SUBTIPOS.TI_COMPANIA ,
    UN_CODIGOPADRE IN ORGANIGRAMA.CODIGO_PADRE%TYPE
  ) RETURN CLOB AS  

   MI_SALIDA     CLOB := ''; 
   MI_HIJOS      CLOB := ''; 
   MI_CANTHIJOS  NUMBER ;
   MI_ESHIJO     BOOLEAN;
   MI_NIVELANT   NUMBER := 1;

  BEGIN

    MI_CANTHIJOS := 0;
    <<RecorrerHijos>>                 
    FOR MI_RS IN (SELECT CODIGO,TIPOLINEA,DESCRIPCION,CODIGO_PADRE,ESTILO 
                  FROM  ORGANIGRAMA 
                  WHERE COMPANIA = UN_COMPANIA 
                    AND CODIGO_PADRE = UN_CODIGOPADRE
                    ORDER BY CODIGO)
    LOOP        
        MI_SALIDA :=MI_SALIDA||' { "id": "'||MI_RS.CODIGO||'", "title": "'||MI_RS.DESCRIPCION||'","type": "'||MI_RS.TIPOLINEA||'","color_nodo":"#'||MI_RS.ESTILO||'",';        
        MI_HIJOS := FC_JSON_ORGANIGRAMARECU(UN_COMPANIA, MI_RS.CODIGO);        
        IF MI_HIJOS IS NOT NULL THEN
            MI_SALIDA := MI_SALIDA || '"children": ['||MI_HIJOS||'] ';

        END IF;  

        MI_SALIDA :=SUBSTR(MI_SALIDA,1,LENGTH(MI_SALIDA)-1)||'},';        


    END LOOP RecorrerHijos;

     MI_SALIDA := SUBSTR(MI_SALIDA,1,LENGTH(MI_SALIDA)-1);   

    RETURN MI_SALIDA;

END FC_JSON_ORGANIGRAMARECU;

END PCK_ORGANIGRAMA;