  FUNCTION FC_MICROMEDICIONSITIO
  (
    /*
      NAME              : FC_MICROMEDICIONSITIO --> EN ACCESS micromedicionSitio
      AUTHORS           : SYSMAN  SAS
      AUTHOR MIGRACION  : YEISSON ALEJANDRO ROJAS RUIZ
      DATE MIGRADOR     : 12/01/2017
      TIME              : 10:00 AM
      SOURCE MODULE     : SERVICIOS PUBLICOS
      MODIFIER          : 
      DATE MODIFIED     : 
      TIME              : 
      DESCRIPTION       : FUNCION QUE RETORNA LA CANTIDAD DE FILAS AFECTADAS POR UNA ACCIÓN DEPENDIENDO DE EL MOMENTO QUE SE INGRESE POR PARAMETRO.
      PARAMETERS        : UN_COMPANIA   => COMPANIA EN LA QUE SE ESTA TRABAJANDO.
                          UN_CICLO      => CICLO POR EL CUAL SE VA A FILTRAR LA INFORMACION.
                          UN_CODIGORUTA => CODIGO RUTA PARA LA CONSULTA DE LOS REGISTROS.
                          UN_ANIO       => NUMERO DEL AÑO POR EL QUE SE VA A FILTRAR.
                          UN_PERIODO    => CADENA DEL PERIODO POR EL QUE SE VA A FILTRAR.
                          UN_MOMENTO    => CARACTER QUE DETERMINA LA ACCION A REALIZAR EN LA FUNCION
      MODIFICATIONS     : 

      @NAME:    obtenerMicromedicion
      @METHOD:  GET
    */
    UN_COMPANIA         IN PCK_SUBTIPOS.TI_COMPANIA, 
    UN_CICLO            IN PCK_SUBTIPOS.TI_CICLO, 
    UN_CODIGORUTA       IN PCK_SUBTIPOS.TI_CODIGORUTA, 
    UN_ANIO             IN PCK_SUBTIPOS.TI_ANIO, 
    UN_PERIODO          IN PCK_SUBTIPOS.TI_PERIODO, 
    UN_MOMENTO          VARCHAR2,
    UN_USUARIO          IN PCK_SUBTIPOS.TI_USUARIO
  ) 
  RETURN NUMBER AS
    MI_MICROMEDICION    PCK_SUBTIPOS.TI_ENTERO;
    MI_RSCONSUMO        SYS_REFCURSOR;
    MI_FILAS            PCK_SUBTIPOS.TI_ENTERO;    
    MI_LECTURA_USUARIO  SP_USUARIO.LECTURA%TYPE;
    MI_STRSQL           PCK_SUBTIPOS.TI_STRSQL;
    MI_CONTEO           PCK_SUBTIPOS.TI_ENTERO;
    MI_TABLA            PCK_SUBTIPOS.TI_STRSQL;
    MI_CAMPOS           PCK_SUBTIPOS.TI_CAMPOS;
    MI_CONDICION        PCK_SUBTIPOS.TI_CONDICION;
    MI_MERGEUSING       PCK_SUBTIPOS.TI_MERGEUSING;
    MI_MERGEENLACE      PCK_SUBTIPOS.TI_MERGEENLACE;
    MI_MERGEEXISTE      PCK_SUBTIPOS.TI_MERGEEXISTE;
    MI_ESTADO           SP_MICROMEDICIONES.ESTADO%TYPE;
    MI_LECTURA_MICRO    SP_MICROMEDICION_CAMBIO.LECTURAINICIAL%TYPE;
    MI_CONSUMO          SP_MICROMEDICION_CAMBIO.LECTURAFINAL%TYPE;

    BEGIN  
      BEGIN
        SELECT  SP_MICROMEDICION_CAMBIO.LECTURAINICIAL, 
                SP_USUARIO.LECTURA
        INTO    MI_LECTURA_MICRO,
                MI_LECTURA_USUARIO
        FROM    SP_USUARIO 
          INNER JOIN SP_MICROMEDICION_CAMBIO 
            ON  SP_USUARIO.COMPANIA   = SP_MICROMEDICION_CAMBIO.COMPANIA
            AND SP_USUARIO.CICLO      = SP_MICROMEDICION_CAMBIO.CICLO
            AND SP_USUARIO.CODIGORUTA = SP_MICROMEDICION_CAMBIO.CODIGORUTA
        WHERE   SP_USUARIO.COMPANIA                    = UN_COMPANIA
          AND   SP_USUARIO.CICLO                       = UN_CICLO
          AND   SP_USUARIO.CODIGORUTA                  = UN_CODIGORUTA
          AND   SP_MICROMEDICION_CAMBIO.CONTROLLECTURA = -1;

        EXCEPTION WHEN NO_DATA_FOUND THEN
          MI_LECTURA_MICRO   := 0;
          MI_LECTURA_USUARIO := 0;
      END;

      CASE 
        WHEN UN_MOMENTO = 'G' THEN --GENERACIÓN DE LOS PLANOS PARA ENVIAR LA LECTURA
          MI_MICROMEDICION := MI_LECTURA_MICRO;

        WHEN UN_MOMENTO = 'R' THEN -- RETORNO DEL PLANO DE FACTURACIÓN EN SITIO (SE DEBE ACTUALIZAR LA LECTURA FINAL DEL MEDIDOR).
          MI_TABLA     := 'SP_MICROMEDICION_CAMBIO';
          MI_CAMPOS    := 'SP_MICROMEDICION_CAMBIO.LECTURAFINAL = '|| MI_LECTURA_USUARIO||'
                          ,MODIFIED_BY                          = '''||UN_USUARIO||'''
                          ,DATE_MODIFIED                        = SYSDATE  ';

          MI_CONDICION := '    SP_MICROMEDICION_CAMBIO.COMPANIA       = '''|| UN_COMPANIA ||'''
                           AND SP_MICROMEDICION_CAMBIO.CICLO          = '  || UN_CICLO || '
                           AND SP_MICROMEDICION_CAMBIO.CONTROLLECTURA = -1';

          BEGIN
            BEGIN
              MI_FILAS     := PCK_DATOS.FC_ACME(UN_TABLA     => MI_TABLA,
                                                UN_ACCION    => 'M',
                                                UN_CAMPOS    => MI_CAMPOS,
                                                UN_CONDICION => MI_CONDICION);
              EXCEPTION WHEN PCK_EXCEPCIONES.EXC_ACTUALIZAR THEN
                RAISE PCK_EXCEPCIONES.EXC_FACTURACION_SERV_PUBLICOS;
            END;
            EXCEPTION WHEN PCK_EXCEPCIONES.EXC_FACTURACION_SERV_PUBLICOS THEN
              PCK_ERR_MSG.RAISE_WITH_MSG(
                UN_EXC_COD   => SQLCODE,
                UN_ERROR_COD => PCK_ERRORES.ERR_FACSERP_MICROMED_MMCAMBIO
              );
          END;                                                
          MI_MICROMEDICION := NVL(MI_FILAS, 0);

        WHEN UN_MOMENTO = 'P' THEN -- PREPARAR PARA SIGUIENTE PERIODO
          FOR MI_RS IN (
            SELECT  SP_MICROMEDICIONES.ESTADO
            FROM    SP_USUARIO 
              INNER JOIN SP_MICROMEDICIONES 
                ON  SP_USUARIO.COMPANIA   = SP_MICROMEDICIONES.COMPANIA 
                AND SP_USUARIO.CICLO      = SP_MICROMEDICIONES.CICLO
                AND SP_USUARIO.CODIGORUTA = SP_MICROMEDICIONES.CODIGORUTA
          )
          LOOP  
            MI_TABLA       := 'SP_USUARIO';
            MI_MERGEUSING  := 'SELECT SP_USUARIO.COMPANIA,
                                      SP_USUARIO.CICLO,
                                      SP_USUARIO.CODIGORUTA,
                                      SP_USUARIO.PORMICRO
                               FROM SP_USUARIO 
                                 INNER JOIN SP_MICROMEDICIONES 
                                   ON  SP_USUARIO.COMPANIA   = SP_MICROMEDICIONES.COMPANIA 
                                   AND SP_USUARIO.CICLO      = SP_MICROMEDICIONES.CICLO
                                   AND SP_USUARIO.CODIGORUTA = SP_MICROMEDICIONES.CODIGORUTA
                               WHERE  SP_USUARIO.COMPANIA        =  '''|| UN_COMPANIA ||'''
                                 AND  SP_USUARIO.CICLO           =  '  || UN_CICLO ||'
                                 AND  SP_MICROMEDICIONES.ANO     <= '  || UN_ANIO ||'
                                 AND  SP_MICROMEDICIONES.PERIODO <= '''|| UN_PERIODO ||'''';

            MI_MERGEENLACE := '    TABLA.COMPANIA   = VISTA.COMPANIA 
                               AND TABLA.CICLO      = VISTA.CICLO
                               AND TABLA.CODIGORUTA = VISTA.CODIGORUTA';

            MI_MERGEEXISTE := 'UPDATE 
                                SET TABLA.PORMICRO = '||PCK_SYSMAN_UTL.FC_IIF(UN_CONDICION => MI_RS.ESTADO = 'C',
                                                                              UN_SI        => 0,
                                                                              UN_NO        => -1)||'
                                    ,TABLA.MODIFIED_BY    = '''||UN_USUARIO||'''
                                    ,TABLA.DATE_MODIFIED  = SYSDATE  ';
            BEGIN
              BEGIN
                MI_FILAS     := PCK_DATOS.FC_ACME(UN_TABLA       => MI_TABLA,
                                                  UN_ACCION      => 'MM',
                                                  UN_MERGEUSING  => MI_MERGEUSING, 
                                                  UN_MERGEENLACE => MI_MERGEENLACE,
                                                  UN_MERGEEXISTE => MI_MERGEEXISTE);

                EXCEPTION WHEN PCK_EXCEPCIONES.EXC_MERGE THEN
                  RAISE PCK_EXCEPCIONES.EXC_FACTURACION_SERV_PUBLICOS;
              END;
              EXCEPTION WHEN PCK_EXCEPCIONES.EXC_FACTURACION_SERV_PUBLICOS THEN
                PCK_ERR_MSG.RAISE_WITH_MSG(
                  UN_EXC_COD   => SQLCODE,
                  UN_ERROR_COD => PCK_ERRORES.ERR_FACSERP_MICROMED_MMUSUARIO
                );
            END;

          END LOOP;                                    
          MI_MICROMEDICION := NVL(MI_FILAS, 0);

          WHEN UN_MOMENTO = 'C' THEN -- CALCULO DE METROS ACUMULADOS A COBRAR
            BEGIN
              MI_STRSQL := 'SELECT  SUM(SP_MICROMEDICION_CAMBIO.LECTURAFINAL - SP_MICROMEDICION_CAMBIO.LECTURAINICIAL) CONSUMO 
                            FROM    SP_MICROMEDICION_CAMBIO 
                            WHERE   SP_MICROMEDICION_CAMBIO.COMPANIA     = ''' || UN_COMPANIA ||'''
                              AND   SP_MICROMEDICION_CAMBIO.CICLO        = '   || UN_CICLO ||'
                              AND   SP_MICROMEDICION_CAMBIO.PERIODO      = ''' || UN_PERIODO ||'''
                              AND   SP_MICROMEDICION_CAMBIO.ANO          = '   || UN_ANIO ||'
                              AND   SP_MICROMEDICION_CAMBIO.CODIGORUTA   = ''' || UN_CODIGORUTA ||'''
                              AND   SP_MICROMEDICION_CAMBIO.LECTURAFINAL > 0
                            GROUP BY SP_MICROMEDICION_CAMBIO.CICLO, 
                                    SP_MICROMEDICION_CAMBIO.CODIGORUTA';

              EXECUTE IMMEDIATE 'SELECT COUNT(1) CONTEO 
                                 FROM   (' || MI_STRSQL || ')' 
                                 INTO   MI_CONTEO;

              EXCEPTION WHEN NO_DATA_FOUND THEN
                MI_CONTEO := 0; 
            END;
            IF MI_CONTEO > 0 THEN
              <<CONSUMO>>
              OPEN MI_RSCONSUMO FOR MI_STRSQL;
              LOOP
                FETCH MI_RSCONSUMO 
                INTO  MI_CONSUMO;
                EXIT WHEN MI_RSCONSUMO%NOTFOUND; 
                <<ESTADO>>
                FOR MI_RSESTADO IN (
                  SELECT  ESTADO 
                  FROM    SP_MICROMEDICIONES
                  WHERE   COMPANIA   = UN_COMPANIA
                    AND   CICLO      = UN_CICLO
                    AND   CODIGORUTA = UN_CODIGORUTA
                )
                LOOP
                  MI_TABLA     := 'SP_USUARIO';
                  MI_CAMPOS    := 'SP_USUARIO.METROSMICRO = ' || NVL(MI_CONSUMO, 0) ||',
                                   SP_USUARIO.PORMICRO    = ' || PCK_SYSMAN_UTL.FC_IIF(UN_CONDICION => MI_RSESTADO.ESTADO = 'A',
                                                                                    UN_SI        => '-1',
                                                                                    UN_NO        => '0')||'
                                  ,MODIFIED_BY    = '''||UN_USUARIO||'''
                                  ,DATE_MODIFIED  = SYSDATE  ';

                  MI_CONDICION := '    SP_USUARIO.COMPANIA   = '''|| UN_COMPANIA ||'''
                                   AND SP_USUARIO.CICLO      = '  || UN_CICLO ||'
                                   AND SP_USUARIO.CODIGORUTA = '''|| UN_CODIGORUTA ||'''';

                  BEGIN
                    BEGIN
                      MI_FILAS     := PCK_DATOS.FC_ACME(UN_TABLA     => MI_TABLA,
                                                        UN_ACCION    => 'M',
                                                        UN_CAMPOS    => MI_CAMPOS,
                                                        UN_CONDICION => MI_CONDICION);

                      EXCEPTION WHEN PCK_EXCEPCIONES.EXC_ACTUALIZAR THEN
                        RAISE PCK_EXCEPCIONES.EXC_FACTURACION_SERV_PUBLICOS;
                    END;
                    EXCEPTION WHEN PCK_EXCEPCIONES.EXC_FACTURACION_SERV_PUBLICOS THEN
                      PCK_ERR_MSG.RAISE_WITH_MSG(
                        UN_EXC_COD   => SQLCODE,
                        UN_ERROR_COD => PCK_ERRORES.ERR_FACSERP_MICROMED_MMUSUARIO
                      );
                  END;
                END LOOP ESTADO;

                MI_MICROMEDICION := PCK_SYSMAN_UTL.FC_IIF(UN_CONDICION => MI_FILAS > 0,
                                                          UN_SI        => MI_CONSUMO,
                                                          UN_NO        => 0);
              END LOOP CONSUMO;
            ELSE
              MI_MICROMEDICION := 0;
            END IF;
      END CASE;

      RETURN MI_MICROMEDICION;
  END FC_MICROMEDICIONSITIO;

  FUNCTION FC_PLANOMULTIUSUARIOS
  (
    /*
      NAME              : FC_PLANOMULTIUSUARIOS --> EN ACCESS PLANOMULTIUSUARIOS
      AUTHORS           : SYSMAN  SAS
      AUTHOR MIGRACION  : YEISSON ALEJANDRO ROJAS RUIZ
      DATE MIGRADOR     : 12/01/2017
      TIME              : 04:00 PM
      SOURCE MODULE     : SERVICIOS PUBLICOS
      MODIFIER          : 
      DATE MODIFIED     : 
      TIME              : 
      DESCRIPTION       : FUNCION QUE RETORNA UNA CADENA DE VALORES CONCATENADOS DE LAS TABLAS DE USUARIOS Y TARIFAS.
      PARAMETERS        : UN_COMPANIA             => COMPANIA EN LA QUE SE ESTA TRABAJANDO.
                          UN_CODIGORUTA           => CODIGORUTA PARA LA CONSULTA DE LOS REGISTROS.
                          UN_ANIO                 => NUMERO DEL AÑO POR EL QUE SE VA A FILTRAR.
                          UN_PERIODO              => CADENA DEL PERIODO POR EL QUE SE VA A FILTRAR.
                          UN_NUMERO_MULTIUSUARIOS => ENTERO QUE DETERMINA COMO SE VA A ARMAR LA CADENA.
      MODIFICATIONS     : 

      @NAME:    armarPlanoMultiusuarios
      @METHOD:  GET
    */
    UN_COMPANIA                 IN PCK_SUBTIPOS.TI_COMPANIA, 
    UN_CODIGORUTA               IN PCK_SUBTIPOS.TI_CODIGORUTA, 
    UN_ANIO                     IN PCK_SUBTIPOS.TI_ANIO, 
    UN_PERIODO                  IN PCK_SUBTIPOS.TI_PERIODO, 
    UN_NUMERO_MULTIUSUARIOS     IN PCK_SUBTIPOS.TI_ENTERO
  ) 
  RETURN VARCHAR2 AS
    MI_CARGOFIJO                PCK_SUBTIPOS.TI_DOBLE;
    MI_SUBFIJO                  PCK_SUBTIPOS.TI_DOBLE;
    MI_SOBREFIJO                PCK_SUBTIPOS.TI_DOBLE;
    MI_CONSUMOBASICO            PCK_SUBTIPOS.TI_DOBLE;
    MI_TARIFABASICO             PCK_SUBTIPOS.TI_DOBLE;
    MI_SUBBASICO                PCK_SUBTIPOS.TI_DOBLE;
    MI_SOBREBASICO              PCK_SUBTIPOS.TI_DOBLE;
    MI_CONSUMOCOMPLEMENTARIO    PCK_SUBTIPOS.TI_DOBLE;
    MI_TARIFACOMPLEMENTARIO     PCK_SUBTIPOS.TI_DOBLE;
    MI_SUBCOMPLEMENTARIO        PCK_SUBTIPOS.TI_DOBLE;
    MI_SOBRECOMPLEMENTARIO      PCK_SUBTIPOS.TI_DOBLE;
    MI_CONSUMOSUNTUARIO         PCK_SUBTIPOS.TI_DOBLE;
    MI_TARIFASUNTUARIO          PCK_SUBTIPOS.TI_DOBLE;
    MI_SUBSUNTUARIO             PCK_SUBTIPOS.TI_DOBLE;
    MI_SOBRESUNTUARIO           PCK_SUBTIPOS.TI_DOBLE;
    MI_SINMEDICION              PCK_SUBTIPOS.TI_DOBLE;
    MI_SUBSINMEDICION           PCK_SUBTIPOS.TI_DOBLE;
    MI_SOBRESINMEDICION         PCK_SUBTIPOS.TI_DOBLE;
    MI_CARGOFIJOALC             PCK_SUBTIPOS.TI_DOBLE;
    MI_SUBFIJOALC               PCK_SUBTIPOS.TI_DOBLE;
    MI_SOBREFIJOALC             PCK_SUBTIPOS.TI_DOBLE;
    MI_TARIFABASICOALC          PCK_SUBTIPOS.TI_DOBLE;
    MI_SUBBASICOALC             PCK_SUBTIPOS.TI_DOBLE;
    MI_SOBREBASICOALC           PCK_SUBTIPOS.TI_DOBLE;
    MI_TARIFACOMPLEMENTARIOALC  PCK_SUBTIPOS.TI_DOBLE;
    MI_SUBCOMPLEMENTARIOALC     PCK_SUBTIPOS.TI_DOBLE;
    MI_SOBRECOMPLEMENTARIOALC   PCK_SUBTIPOS.TI_DOBLE;
    MI_TARIFASUNTUARIOALC       PCK_SUBTIPOS.TI_DOBLE;
    MI_SUBSUNTUARIOALC          PCK_SUBTIPOS.TI_DOBLE;
    MI_SOBRESUNTUARIOALC        PCK_SUBTIPOS.TI_DOBLE;
    MI_SINMEDICIONALC           PCK_SUBTIPOS.TI_DOBLE;
    MI_SUBSINMEDICIONALC        PCK_SUBTIPOS.TI_DOBLE;
    MI_SOBRESINMEDICIONALC      PCK_SUBTIPOS.TI_DOBLE;
    MI_CONSUMOBASICOALC         PCK_SUBTIPOS.TI_DOBLE;
    MI_CONSUMOCOMPLEMENTARIOALC PCK_SUBTIPOS.TI_DOBLE;
    MI_CONSUMOSUNTUARIOALC      PCK_SUBTIPOS.TI_DOBLE;
    MI_PLANOMULTIUSUARIOS       VARCHAR2(3200 CHAR);  -- VARCHAR2 DEBIDO A QUE NO EXISTE UN TIPO DE DATO CON LAS MISMAS CARACTERISTICAS EN PCK_SUBTIPOS.

  BEGIN
    MI_CARGOFIJO               := 0;
    MI_SUBFIJO                 := 0;
    MI_SOBREFIJO               := 0;
    MI_CONSUMOBASICO           := 0;
    MI_TARIFABASICO            := 0;
    MI_SUBBASICO               := 0;
    MI_SOBREBASICO             := 0;
    MI_CONSUMOCOMPLEMENTARIO   := 0;
    MI_TARIFACOMPLEMENTARIO    := 0;
    MI_SUBCOMPLEMENTARIO       := 0;
    MI_SOBRECOMPLEMENTARIO     := 0;
    MI_CONSUMOSUNTUARIO        := 0;
    MI_TARIFASUNTUARIO         := 0;
    MI_SUBSUNTUARIO            := 0;
    MI_SOBRESUNTUARIO          := 0;
    MI_SINMEDICION             := 0;
    MI_SUBSINMEDICION          := 0;
    MI_SOBRESINMEDICION        := 0; 
    MI_CARGOFIJOALC            := 0;
    MI_SUBFIJOALC              := 0;
    MI_SOBREFIJOALC            := 0;
    MI_TARIFABASICOALC         := 0;
    MI_SUBBASICOALC            := 0;
    MI_SOBREBASICOALC          := 0;
    MI_TARIFACOMPLEMENTARIOALC := 0;
    MI_SUBCOMPLEMENTARIOALC    := 0;
    MI_SOBRECOMPLEMENTARIOALC  := 0;
    MI_TARIFASUNTUARIOALC      := 0;
    MI_SUBSUNTUARIOALC         := 0;
    MI_SOBRESUNTUARIOALC       := 0;
    MI_SINMEDICIONALC          := 0;
    MI_SUBSINMEDICIONALC       := 0;
    MI_SOBRESINMEDICIONALC     := 0;

    IF UN_NUMERO_MULTIUSUARIOS <> 0 THEN
      FOR MI_RS IN(
        SELECT  SP_MULTIUSUARIOS.USO, 
                SP_MULTIUSUARIOS.ESTRATO, 
                SP_MULTIUSUARIOS.PORCENTAJE, 
                SP_MULTIUSUARIOS.CANTIDADTOTAL
        FROM    SP_MULTIUSUARIOS
        WHERE   SP_MULTIUSUARIOS.COMPANIA   = UN_COMPANIA
        AND     SP_MULTIUSUARIOS.CODIGORUTA = UN_CODIGORUTA
      )
      LOOP
      MI_PLANOMULTIUSUARIOS := ';' || MI_RS.USO || ';' 
                               || MI_RS.ESTRATO || ';' 
                               || MI_RS.PORCENTAJE || ';' 
                               || MI_RS.CANTIDADTOTAL;
        FOR MI_RSTARIFAS IN(
          SELECT  CARGOFIJO,
                  SUBFIJO,
                  SOBREFIJO,
                  CONSUMOBASICO,
                  TARIFABASICO,
                  SUBBASICO,
                  SOBREBASICO,
                  CONSUMOCOMPLEMENTARIO,
                  TARIFACOMPLEMENTARIO,
                  SUBCOMPLEMENTARIO,
                  SOBRECOMPLEMENTARIO,
                  CONSUMOSUNTUARIO,
                  TARIFASUNTUARIO,
                  SUBSUNTUARIO,
                  SOBRESUNTUARIO,
                  SINMEDICION,
                  SUBSINMEDICION,
                  SOBRESINMEDICION,
                  FIJOALCANTARILLADO,
                  SUBALCFIJO,
                  SOBREALCFIJO,
                  ALCBASICO,
                  SUBALCBASICO,
                  SOBREALCBASICO,
                  ALCCOMPLEMENTARIO,
                  SUBALCCOMPLEMENTARIO,
                  SOBREALCCOMPLEMENTARIO,
                  ALCSUNTUARIO,
                  SUBALCSUNTUARIO,
                  SOBREALCSUNTUARIO,
                  SINMEDICIONALC,
                  SUBALCSINMEDICION,
                  SOBREALCSINMEDICION
          FROM    SP_TARIFAS
          WHERE   COMPANIA = UN_COMPANIA 
            AND   ANO      = UN_ANIO
            AND   PERIODO  = UN_PERIODO
            AND   USO      = MI_RS.USO
            AND   ESTRATO  = MI_RS.ESTRATO
        )
        LOOP
          -- ACUEDUCTO
          MI_CARGOFIJO             := MI_CARGOFIJO + NVL(MI_RSTARIFAS.CARGOFIJO, 0) * MI_RS.PORCENTAJE;
          MI_SUBFIJO               := MI_SUBFIJO + NVL(MI_RSTARIFAS.SUBFIJO, 0) * MI_RS.PORCENTAJE;
          MI_SOBREFIJO             := MI_SOBREFIJO + NVL(MI_RSTARIFAS.SOBREFIJO, 0) * MI_RS.PORCENTAJE;
          MI_CONSUMOBASICO         := MI_CONSUMOBASICO + NVL(MI_RSTARIFAS.CONSUMOBASICO, 0) * MI_RS.PORCENTAJE;
          MI_TARIFABASICO          := MI_TARIFABASICO + NVL(MI_RSTARIFAS.TARIFABASICO, 0) * MI_RS.PORCENTAJE;
          MI_SUBBASICO             := MI_SUBBASICO + NVL(MI_RSTARIFAS.SUBBASICO, 0) * MI_RS.PORCENTAJE;
          MI_SOBREBASICO           := MI_SOBREBASICO + NVL(MI_RSTARIFAS.SOBREBASICO, 0) * MI_RS.PORCENTAJE;
          MI_CONSUMOCOMPLEMENTARIO := MI_CONSUMOCOMPLEMENTARIO + NVL(MI_RSTARIFAS.CONSUMOCOMPLEMENTARIO, 0) * MI_RS.PORCENTAJE;
          MI_TARIFACOMPLEMENTARIO  := MI_TARIFACOMPLEMENTARIO + NVL(MI_RSTARIFAS.TARIFACOMPLEMENTARIO, 0) * MI_RS.PORCENTAJE;
          MI_SUBCOMPLEMENTARIO     := MI_SUBCOMPLEMENTARIO + NVL(MI_RSTARIFAS.SUBCOMPLEMENTARIO, 0) * MI_RS.PORCENTAJE;
          MI_SOBRECOMPLEMENTARIO   := MI_SOBRECOMPLEMENTARIO + NVL(MI_RSTARIFAS.SOBRECOMPLEMENTARIO, 0) * MI_RS.PORCENTAJE;
          MI_CONSUMOSUNTUARIO      := MI_CONSUMOSUNTUARIO + NVL(MI_RSTARIFAS.CONSUMOSUNTUARIO, 0) * MI_RS.PORCENTAJE;
          MI_TARIFASUNTUARIO       := MI_TARIFASUNTUARIO + NVL(MI_RSTARIFAS.TARIFASUNTUARIO, 0) * MI_RS.PORCENTAJE;
          MI_SUBSUNTUARIO          := MI_SUBSUNTUARIO + NVL(MI_RSTARIFAS.SUBSUNTUARIO, 0) * MI_RS.PORCENTAJE;
          MI_SOBRESUNTUARIO        := MI_SOBRESUNTUARIO + NVL(MI_RSTARIFAS.SOBRESUNTUARIO, 0) * MI_RS.PORCENTAJE;
          MI_SINMEDICION           := MI_SINMEDICION + NVL(MI_RSTARIFAS.SINMEDICION, 0) * MI_RS.PORCENTAJE;
          MI_SUBSINMEDICION        := MI_SUBSINMEDICION + NVL(MI_RSTARIFAS.SUBSINMEDICION, 0) * MI_RS.PORCENTAJE;
          MI_SOBRESINMEDICION      := MI_SOBRESINMEDICION + NVL(MI_RSTARIFAS.SOBRESINMEDICION, 0) * MI_RS.PORCENTAJE;
          -- ALCANTARILLADO
          MI_CARGOFIJOALC             := MI_CARGOFIJOALC + NVL(MI_RSTARIFAS.FIJOALCANTARILLADO, 0) * MI_RS.PORCENTAJE;
