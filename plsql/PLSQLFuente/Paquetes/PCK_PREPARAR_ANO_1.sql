                           --AND PP.SALDO13 NOT IN(0)  
                           AND PP.CODIGO NOT IN(SELECT CODIGO      
                                         FROM PLAN_CONTABLE  
                                         WHERE COMPANIA = UN_COMPANIA_DESTINO 
                                           AND ANO      = UN_ANO_DESTINO 
                                           AND CODIGO   = PP.CODIGO) 
                    )
                SELECT *
