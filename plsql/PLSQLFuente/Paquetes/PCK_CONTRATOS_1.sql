FUNCTION FC_ARCHIVOPLANOCAMARACOMERCIO
/*
    NAME              : FC_GENERARARCHIVOPLANO --> EN ACCESS Relacionado con - Impresora_Click Formulario FrmPlanoSemCamComercio
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : YESIKA PAOLA BECERRA CASTRO
