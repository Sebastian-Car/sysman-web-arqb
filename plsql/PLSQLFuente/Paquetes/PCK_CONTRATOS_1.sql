FUNCTION FC_OBT_TIPOSEGUIMIENTO 
  (
   UN_INTERVENTOR IN TERCERO.NIT%TYPE,
   UN_CEDULA      IN TERCERO.NIT%TYPE
)
RETURN VARCHAR2 AS 
  MI_TIPO_SEGUIMIENTO     VARCHAR2(2000 CHAR);
BEGIN

   IF UN_INTERVENTOR IS NOT NULL AND UN_CEDULA IS NULL THEN
        MI_TIPO_SEGUIMIENTO := 'INTERVENTOR';
    ELSE IF UN_INTERVENTOR IS NULL AND UN_CEDULA IS NOT NULL THEN
        MI_TIPO_SEGUIMIENTO := 'SUPERVISOR';
    ELSE IF UN_INTERVENTOR IS NOT NULL AND UN_CEDULA IS NOT NULL THEN
        MI_TIPO_SEGUIMIENTO := 'INTERVENTOR Y SUPERVISOR';
    ELSE IF UN_INTERVENTOR IS NULL AND UN_CEDULA IS NULL THEN
        MI_TIPO_SEGUIMIENTO := 'NO SE DILIGENCIA INFORMACIÓN PARA ESTE FORMULARIO EN ESTE PERÍODO DE REPORTE';
    END IF;
    END IF;
    END IF;
    END IF;
    
    RETURN MI_TIPO_SEGUIMIENTO; 
END FC_OBT_TIPOSEGUIMIENTO;


FUNCTION FC_OBT_TIPOADICION 
  (
   UN_VALORTOTAL  IN PCK_SUBTIPOS.TI_DOBLE,
   UN_VALORFINAL      IN PCK_SUBTIPOS.TI_DOBLE,
   UN_FECHAFINAL  IN DATE
)
RETURN VARCHAR2 AS 
  MI_TIPO_ADICION     VARCHAR2(2000 CHAR);
BEGIN

   IF (UN_VALORTOTAL - UN_VALORFINAL <> 0) AND UN_FECHAFINAL IS NULL THEN 
        MI_TIPO_ADICION := 'ADICIÓN EN VALOR';
   ELSE IF (UN_VALORTOTAL - UN_VALORFINAL = 0) AND UN_FECHAFINAL IS NOT NULL THEN
        MI_TIPO_ADICION := 'ADICIÓN EN TIEMPO';
   ELSE IF (UN_VALORTOTAL - UN_VALORFINAL <> 0) AND UN_FECHAFINAL IS NOT NULL THEN
         MI_TIPO_ADICION := 'ADICIÓN EN VALOR y EN TIEMPO';
   ELSE IF (UN_VALORTOTAL - UN_VALORFINAL = 0) AND UN_FECHAFINAL IS NULL THEN
         MI_TIPO_ADICION := 'NO SE HA ADICIONADO NI EN VALOR y EN TIEMPO';
   END IF;
   END IF;
   END IF;
   END IF;
    
    RETURN MI_TIPO_ADICION; 
END FC_OBT_TIPOADICION;

