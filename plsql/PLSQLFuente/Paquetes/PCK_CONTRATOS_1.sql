    @NAME: getPlanoComercio
  */
(
  UN_COMPANIA        IN PCK_SUBTIPOS.TI_COMPANIA,
  UN_CARGO           IN PCK_SUBTIPOS.TI_PARAMETRO,
  UN_SEMESTRE        IN PCK_SUBTIPOS.TI_ENTERO,
  UN_ANIO            IN ANO.NUMERO%TYPE,
  UN_CAMCOMERCIO     IN VARCHAR2,
  UN_CONPROPONENTES  IN PCK_SUBTIPOS.TI_ENTERO,
  UN_SECXREG         IN PCK_SUBTIPOS.TI_ENTERO,
  UN_NIT             IN PCK_SUBTIPOS.TI_TERCERO,
  UN_NOMBRE          IN VARCHAR2,
  UN_CIUDAD          IN VARCHAR2,
  UN_DIRECCION       IN VARCHAR2,
  UN_FUNCIONARIO     IN PCK_SUBTIPOS.TI_PARAMETRO,
  UN_FECHA           IN DATE,
  UN_MODULO          IN PCK_SUBTIPOS.TI_ENTERO,
  UN_USUARIO         IN PCK_SUBTIPOS.TI_USUARIO
)
RETURN CLOB
AS
   MI_REGISTROMERCANTIL TERCERO.REGISTROMERCANTIL%TYPE;
   MI_TERCERO           ORDENDECOMPRA.TERCERO%TYPE;
   MI_NOMBRE            TERCERO.NOMBRE%TYPE;
   MI_NUMERO            ORDENDECOMPRA.NUMERO%TYPE;
   MI_FECHA             ORDENDECOMPRA.FECHA%TYPE;
   MI_FECHAFINALIZACION ORDENDECOMPRA.FECHAFINALIZACION%TYPE;
   MI_VALORTOTAL        ORDENDECOMPRA.VALORTOTAL%TYPE;
   MI_CUANTIA           VARCHAR2(250 CHAR);
   MI_CUMPLIMIENTO      ORDENDECOMPRA.CUMPLIMIENTO%TYPE;
   MI_SSQL              PCK_SUBTIPOS.TI_STRSQL;
   MI_I                 PCK_SUBTIPOS.TI_ENTERO:=0;
   MI_NUMREGISTRO       PCK_SUBTIPOS.TI_ENTERO:=0;
   MI_SALMIN            ANO.SALARIOMINIMO%TYPE;
   MI_RCS               SYS_REFCURSOR;
   MI_VCADENA           CLOB;
   MI_CLASIFICACIONCC   ORDENDECOMPRA.CLASIFICACIONCC%TYPE;
   MI_FECHA1            VARCHAR2(10);
   MI_FECHA2            VARCHAR2(10);
   MI_CAMPOS            PCK_SUBTIPOS.TI_CAMPOS;    
   MI_CONDICION         PCK_SUBTIPOS.TI_CONDICION;
   MI_MSGERROR          PCK_SUBTIPOS.TI_CLAVEVALOR;
BEGIN

  IF UN_SEMESTRE = 1 THEN
    MI_FECHA1 := '01/01/' || UN_ANIO;
    MI_FECHA2 := '30/06/' || UN_ANIO;
  ELSE
    MI_FECHA1 := '01/07/' || UN_ANIO;
    MI_FECHA2 := '31/12/' || UN_ANIO;
  END IF;

  --  SE ACTUALIZA EL FUNCIONARIO
  BEGIN
    MI_CAMPOS    := 'VALOR = ''' || UN_FUNCIONARIO || '''
                   , MODIFIED_BY = ''' || UN_USUARIO || '''
                   , DATE_MODIFIED = SYSDATE' ;
    MI_CONDICION := 'NOMBRE = '''|| 'ARCHIVO CAMARA COMERCIO - FUNCIONARIO REPORTA' ||'''
                     AND COMPANIA = '''|| UN_COMPANIA ||''' ';
    BEGIN
    PCK_DATOS.GL_RTA:= PCK_DATOS.FC_ACME(UN_TABLA      =>  'PARAMETRO',
                                         UN_ACCION     =>  'M',
                                         UN_CAMPOS     =>  MI_CAMPOS,
                                         UN_CONDICION  =>  MI_CONDICION);
    EXCEPTION WHEN PCK_EXCEPCIONES.EXC_ACTUALIZAR THEN
          RAISE PCK_EXCEPCIONES.EXC_CONTROLCONTRATOS;  
    END;
    EXCEPTION WHEN PCK_EXCEPCIONES.EXC_CONTROLCONTRATOS THEN
      MI_MSGERROR (1).CLAVE := 'PARAMETRO';
      MI_MSGERROR (1).VALOR := 'ARCHIVO CAMARA COMERCIO - FUNCIONARIO REPORTA';
      PCK_ERR_MSG.RAISE_WITH_MSG (UN_EXC_COD    => SQLCODE,
                                  UN_ERROR_COD  => PCK_ERRORES.ERRR_CONTRATOS_ACTPARAMETROS,
                                  UN_TABLAERROR => 'PARAMETRO',
                                  UN_REEMPLAZOS => MI_MSGERROR);
  END;

  -- SE ACTUALIZA EL CARGO
  BEGIN
    MI_CAMPOS    := 'VALOR = ''' || UN_CARGO || '''
                   , MODIFIED_BY = ''' || UN_USUARIO || '''
                   , DATE_MODIFIED = SYSDATE' ;
    MI_CONDICION := 'NOMBRE = '''|| 'ARCHIVO CAMARA COMERCIO - CARGO FUNCIONARIO REPORTA' ||'''
                     AND COMPANIA = '''|| UN_COMPANIA ||''' ';
    BEGIN
    PCK_DATOS.GL_RTA:= PCK_DATOS.FC_ACME(UN_TABLA      =>  'PARAMETRO',
                                         UN_ACCION     =>  'M',
                                         UN_CAMPOS     =>  MI_CAMPOS,
                                         UN_CONDICION  =>  MI_CONDICION);
    EXCEPTION WHEN PCK_EXCEPCIONES.EXC_ACTUALIZAR THEN
          RAISE PCK_EXCEPCIONES.EXC_CONTROLCONTRATOS;  
    END;
    EXCEPTION WHEN PCK_EXCEPCIONES.EXC_CONTROLCONTRATOS THEN
      MI_MSGERROR (1).CLAVE := 'PARAMETRO';
      MI_MSGERROR (1).VALOR := 'ARCHIVO CAMARA COMERCIO - CARGO FUNCIONARIO REPORTA';
      PCK_ERR_MSG.RAISE_WITH_MSG (UN_EXC_COD    => SQLCODE,
                                  UN_ERROR_COD  => PCK_ERRORES.ERRR_CONTRATOS_ACTPARAMETROS,
                                  UN_TABLAERROR => 'PARAMETRO',
                                  UN_REEMPLAZOS => MI_MSGERROR);
  END;

  MI_SSQL :='SELECT ORDENDECOMPRA.TERCERO,
                    ORDENDECOMPRA.NUMERO,
                    ORDENDECOMPRA.FECHAFINALIZACION,
                    ORDENDECOMPRA.VALORTOTAL,
                    ORDENDECOMPRA.CUMPLIMIENTO,
                    TERCERO.REGISTROMERCANTIL,
                    ORDENDECOMPRA.FECHA,
                    TERCERO.NOMBRE
             FROM  (ORDENDECOMPRA
              LEFT JOIN TERCERO
                ON  ORDENDECOMPRA.COMPANIA  = TERCERO.COMPANIA
                AND ORDENDECOMPRA.TERCERO   = TERCERO.NIT)
              LEFT JOIN TIPOORDENDECOMPRA
                ON ORDENDECOMPRA.CLASEORDEN           = TIPOORDENDECOMPRA.CODIGO
                AND ORDENDECOMPRA.COMPANIA            = TIPOORDENDECOMPRA.COMPANIA
             WHERE ORDENDECOMPRA.COMPANIA             = '''|| UN_COMPANIA || '''
               AND ORDENDECOMPRA.FECHA                BETWEEN ' || PCK_SYSMAN_UTL.FC_SDATE (UN_FECHA   => MI_FECHA1,
                                                                                            UN_FORMATO => 'DD/MM/YYYY')
                                                       ||' AND '|| PCK_SYSMAN_UTL.FC_SDATE (UN_FECHA   =>  MI_FECHA2,
                                                                                            UN_FORMATO => 'DD/MM/YYYY')||'
               AND ORDENDECOMPRA.ESTADO               <> ''A''
               AND NVL(TIPOORDENDECOMPRA.CLASE,'''')  <> ''M''';
  IF UN_CONPROPONENTES = 1 THEN
    MI_SSQL := MI_SSQL || ' AND  ORDENDECOMPRA.CLASIFICACIONCC IS NOT NULL';
  END IF;
  MI_SSQL := MI_SSQL || ' ORDER BY ORDENDECOMPRA.NUMERO';


  MI_I := TO_NUMBER(PCK_SYSMAN_UTL.FC_PAR(UN_COMPANIA  =>  UN_COMPANIA,
                                          UN_NOMBRE    =>  'CONSECUTIVO ARCHIVO PLANO',
                                          UN_MODULO    =>  UN_MODULO,
                                          UN_FECHA_PAR => SYSDATE)) + 1;
  SELECT  SALARIOMINIMO
  INTO    MI_SALMIN
  FROM ANO
  WHERE COMPANIA = ''||UN_COMPANIA||''
    AND NUMERO   = UN_ANIO;
  IF MI_SALMIN = 0 THEN
    PCK_DATOS.GL_ERROR_MSG := 'El salario minimo no ha sido asignado para el anio ' || UN_ANIO;
    RETURN PCK_DATOS.GL_ERROR_MSG;
  END IF;

  <<ARCHIVOPLANO>>
  OPEN MI_RCS FOR MI_SSQL;
  LOOP
    FETCH MI_RCS INTO MI_TERCERO , MI_NUMERO,  MI_FECHAFINALIZACION , MI_VALORTOTAL,MI_CUMPLIMIENTO,MI_REGISTROMERCANTIL,MI_FECHA,MI_NOMBRE;
    EXIT WHEN MI_RCS%NOTFOUND;
      MI_NUMREGISTRO:=MI_NUMREGISTRO+1;
      MI_VCADENA := MI_VCADENA || LPAD(TO_NUMBER(MI_I),4,'0') || 'B' || LPAD(NVL(MI_REGISTROMERCANTIL,'0'),8,'0');
      MI_VCADENA := MI_VCADENA || RPAD(MI_TERCERO, 12, '0') || RPAD(MI_NOMBRE, 65, ' ');
      MI_VCADENA := MI_VCADENA || RPAD(MI_NUMERO, 15 , ' ') || RPAD(TO_CHAR(MI_FECHA, 'YYYYMMDD'),8,'0');
      MI_VCADENA := MI_VCADENA || RPAD(TO_CHAR(MI_FECHAFINALIZACION,'YYYYMMDD'),8,'0');
      MI_CUANTIA := PCK_SYSMAN_UTL.FC_ROUND( UN_VALOR     => MI_VALORTOTAL / MI_SALMIN,
                                             UN_PRECISION => 2);
      MI_CUANTIA := LPAD(TO_NUMBER(MI_CUANTIA), 13 ,'0') || LPAD(PCK_SYSMAN_UTL.FC_ROUND( UN_VALOR     => MI_CUANTIA - TO_NUMBER(MI_CUANTIA),
                                                                                          UN_PRECISION => 2) * 100,2,'0');
      MI_VCADENA := MI_VCADENA || LPAD(REPLACE (TO_NUMBER(MI_CUANTIA),'.',''),15, '0');
      MI_VCADENA := MI_VCADENA || RPAD(NVL(MI_CUMPLIMIENTO, ' '),1,' ');
      MI_VCADENA := MI_VCADENA || RPAD(' ', 78 ,' ');
      MI_VCADENA := MI_VCADENA || CHR(13) || CHR(10);


      IF MI_NUMREGISTRO = 0 THEN
        RETURN NULL;
      END IF ;

      IF UN_SECXREG = 0 THEN
        MI_I := MI_I + 1;
      END IF;

      MI_VCADENA := MI_VCADENA || PCK_SYSMAN_UTL.FC_STRZERO( UN_NUMERO   => TO_NUMBER(MI_I),
                                                             UN_LONGITUD => 4) || 'C' || LPAD(NVL(MI_CLASIFICACIONCC,0),5,'0') || RPAD(' ',205,' ');
      MI_VCADENA := MI_VCADENA || CHR(13) || CHR(10);
      MI_I := MI_I + 1;

  END LOOP ARCHIVOPLANO;


    IF UN_SECXREG = 0 THEN
      MI_VCADENA := MI_VCADENA || PCK_SYSMAN_UTL.FC_STRZERO( UN_NUMERO   => TO_NUMBER(MI_I),
                                                             UN_LONGITUD => 4) || 'F' || RPAD(' ',210,' ');
