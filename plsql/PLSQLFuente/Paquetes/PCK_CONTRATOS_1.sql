FUNCTION FC_NOPOLIZA
/*
    NAME              : FC_ NOPOLIZA ----> EN ACCESS------ TraerNoPolizas
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : LIZETH CASTRO ORDOÃ?EZ
    DATE MIGRADOR     : 18/09/2015
    TIME              : 04:30 PM
    SOURCE MODULE     : CONTARTOS_PROCESOS - copia.accdb
    MODIFIER          : JULIO CESAR REINA PANCHE
    DATE MODIFIED     : 31/01/2017
    TIME              : 12:01 PM
    MODIFICATIONS     : CORRECCIÓN SEGÚN ESTÁNDAR DE PROGRAMACIÓN Y OPTIMIZACIÓN DEL MANEJO DE ERRORES.
    DESCRIPTION       : RETORNA UNA CADENA PARA UN CAMPO EN UN REPORTE
    PARAMETERS        : UN_CLASEORDEN     => CLASE POR EL CUAL SE VA A FILTRAR LA POLIZAS.
                        UN_ORDENDECOMPRA  => ORDEN DE COMPRA POR EL CUAL SE VA A FILTRAR LA POLIZAS.
                        UN_COMPANIA       => COMPANIA EN LA QUE SE ESTÁ TRABAJANDO.
    @NAME: getPolizaNumero
  */
(
 UN_CLASEORDEN     IN POLIZAS.CLASEORDEN%TYPE,
 UN_ORDENDECOMPRA  IN POLIZAS.ORDENDECOMPRA%TYPE,
 UN_COMPANIA       IN PCK_SUBTIPOS.TI_COMPANIA
)

RETURN VARCHAR2
  AS
    MI_POLIZA        VARCHAR2(3000 CHAR);
    MI_STRSQL        PCK_SUBTIPOS.TI_STRSQL;
    MI_RS            SYS_REFCURSOR;
    MI_DESCRIPCION   TIPOPOLIZA.DESCRIPCION%TYPE;
    MI_CODIGO        POLIZAS.CODIGO%TYPE;
    MI_INICIO        POLIZAS.VIGENCIADESDE%TYPE;
    MI_FIN           POLIZAS.VIGENCIAHASTA%TYPE;
  BEGIN
    MI_STRSQL:='SELECT NVL(TIPOPOLIZA.DESCRIPCION,''''),
                       NVL(POLIZAS.CODIGO,''''),
                       NVL(POLIZAS.VIGENCIADESDE,''''),
                       NVL(POLIZAS.VIGENCIAHASTA,'''')
                FROM   POLIZAS
                  LEFT JOIN TIPOPOLIZA
                    ON POLIZAS.TIPO = TIPOPOLIZA.CODIGO
                WHERE COMPANIA      = '''||UN_COMPANIA||'''
                  AND CLASEORDEN    = '''||UN_CLASEORDEN||'''
                  AND ORDENDECOMPRA = '||UN_ORDENDECOMPRA||' ';
    <<NUMEROPOLIZAS>>
    OPEN MI_RS FOR MI_STRSQL;
    LOOP
    FETCH MI_RS INTO MI_DESCRIPCION, MI_CODIGO, MI_INICIO, MI_FIN;
    EXIT WHEN MI_RS%NOTFOUND;
      MI_POLIZA:= MI_POLIZA || MI_DESCRIPCION ||
                  ' No. '   || MI_CODIGO      ||
                  ' Inicio '|| MI_INICIO      ||
                  ' Fin '   || MI_FIN ;
    END LOOP NUMEROPOLIZAS;
    CLOSE MI_RS;

    RETURN MI_POLIZA;
END FC_NOPOLIZA;

