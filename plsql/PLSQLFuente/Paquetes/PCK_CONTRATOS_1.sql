          MI_POLIZA:=  MI_POLIZA  ||'Vigente';
      ELSE
          MI_POLIZA:=  MI_POLIZA  ||'Sin Vigencia ';
      END IF;
  END LOOP ESTADOPOLIZA;
CLOSE MI_RS;
  RETURN MI_POLIZA;
