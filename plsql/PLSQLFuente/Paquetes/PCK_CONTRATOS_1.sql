    DATE MODIFIED     : 31/01/2017
    TIME              : 04:30 PM
    MODIFICATIONS     : CORRECCIÓN SEGÚN ESTÁNDAR DE PROGRAMACIÓN Y OPTIMIZACIÓN DEL MANEJO DE ERRORES.
    MODIFIER          : SERGIO ESTEBAN PIÑA VARGAS
