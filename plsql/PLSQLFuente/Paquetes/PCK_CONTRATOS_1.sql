                                    AND ORDENDECOMPRA.CLASEORDEN BETWEEN UN_TIPOCONTRATOINI AND UN_TIPOCONTRATOFIN
                                    AND ORDENDECOMPRA.VALORTOTAL > 0
                                    AND CLASETRANSACCIONC.CLASET = 'N'
                                    AND CLASETRANSACCIONC.CLASENOVEDAD = 'A'
                                    GROUP BY ORDENDECOMPRA.CLASEORDEN,
                                    ORDENDECOMPRA.CLASEORDEN,
                                    ORDENDECOMPRA.NUMERO,
                                    ORDENDECOMPRA.VALORTOTAL)

