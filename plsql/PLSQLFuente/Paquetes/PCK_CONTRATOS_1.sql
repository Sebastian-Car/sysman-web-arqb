 MI_STRSQL:='SELECT POLIZAS.ESTADO
             FROM   POLIZAS
               LEFT JOIN TIPOPOLIZA
                ON POLIZAS.TIPO = TIPOPOLIZA.CODIGO
             WHERE COMPANIA      = '''||UN_COMPANIA     ||'''
               AND CLASEORDEN    = '''||UN_CLASEORDEN   ||'''
               AND ORDENDECOMPRA = '  ||UN_ORDENDECOMPRA||' ';
<<ESTADOPOLIZA>>
OPEN MI_RS FOR MI_STRSQL;
