FUNCTION FC_PREPPIVOTGRAFDEPENDENCIAS
/*
    NAME              : NO APLICA
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : JAVIER ANDRES RODRIGUEZ RIOS
    DATE MIGRADOR     : 29/09/2015
    TIME              : 11:30 AM
    SOURCE MODULE     : CONTARTOS_PROCESOS - copia(2).accdb
    MODIFIER          : JULIO CESAR REINA PANCHE
    DATE MODIFIED     : 31/01/2017
    TIME              : 04:00 PM
    MODIFICATIONS     : CORRECCIÓN SEGÚN ESTÁNDAR DE PROGRAMACIÓN Y OPTIMIZACIÓN DEL MANEJO DE ERRORES.
    DESCRIPTION       : RETORNA UNA CADENA PARA UN CAMPO EN UN REPORTE
    PARAMETERS        : UN_COMPANIA    => COMPANIA EN LA QUE SE ESTÁ TRABAJANDO.
                        UN_ANIOINICIAL => ANIO INCIAL POR EL CUAL SE VA A FILTRAR LAS ORDENES DE COMPRA.
                        UN_ANIOFINAL   => ANIO FINAL POR EL CUAL SE VA A FILTRAR LAS ORDENES DE COMPRA.
                        UN_DEPENDENCIA => DEPENDENCIA POR EL CUAL SE VA A FILTRAR LAS ORDENES DE COMPRA.
    @NAME: getPivotDependencias
  */
(
    UN_COMPANIA     IN PCK_SUBTIPOS.TI_COMPANIA,
    UN_ANIOINICIAL  IN PCK_SUBTIPOS.TI_ANIO,
    UN_ANIOFINAL    IN PCK_SUBTIPOS.TI_ANIO,
    UN_DEPENDENCIA  IN ORDENDECOMPRA.DEPENDENCIA%TYPE
)
RETURN VARCHAR2
  AS
    MI_RESULTADO  VARCHAR2(3200 CHAR);
    MI_RES        VARCHAR2(50 CHAR);
BEGIN
  BEGIN
    <<PIVOTDEPENDENCIAS>>
    FOR RS IN
             (SELECT  TIPOORDENDECOMPRA.NOMBRE,
                      COUNT(TIPOORDENDECOMPRA.NOMBRE)
              FROM ORDENDECOMPRA
                INNER JOIN TIPOORDENDECOMPRA
                  ON 	(ORDENDECOMPRA.COMPANIA     = TIPOORDENDECOMPRA.COMPANIA)
                  AND (ORDENDECOMPRA.CLASEORDEN 	= TIPOORDENDECOMPRA.CODIGO)
              WHERE ORDENDECOMPRA.COMPANIA                = UN_COMPANIA
                AND 	TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY') BETWEEN  UN_ANIOINICIAL  AND  UN_ANIOFINAL
                AND 	ORDENDECOMPRA.DEPENDENCIA			      = UN_DEPENDENCIA
                GROUP BY TIPOORDENDECOMPRA.NOMBRE
                )
    LOOP
      MI_RES:=RS.NOMBRE;
      MI_RESULTADO:=MI_RESULTADO||''''||MI_RES|| ''',';
     --  RS.PIV || '''' || ' P' || RS.PIV || ',''';
    END LOOP PIVOTDEPENDENCIAS;
    EXCEPTION WHEN NO_DATA_FOUND THEN
      MI_RESULTADO:='';
  END;
  MI_RESULTADO:= SUBSTR(MI_RESULTADO,1, LENGTH(MI_RESULTADO)-1);
RETURN MI_RESULTADO;
END FC_PREPPIVOTGRAFDEPENDENCIAS;

