                         AND CLASEORDEN ='''|| RS.CLASEORDEN ||'''
                         AND NUMERO     ='|| RS.NUMERO ||'';

              PCK_DATOS.GL_RTA := PCK_DATOS.FC_ACME (UN_TABLA      => 'ORDENDECOMPRA'
                                                    ,UN_ACCION    => 'M'
                                                    ,UN_CAMPOS    => MI_CAMPOS
                                                    ,UN_CONDICION => MI_CONDICION);

