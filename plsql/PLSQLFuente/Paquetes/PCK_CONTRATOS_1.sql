    BEGIN

            FOR  RS IN (SELECT ORDENDECOMPRA.CLASEORDEN,
                                    ORDENDECOMPRA.NUMERO,
                                    ROUND(SUM((NOVEDADCONTRATO.VALORTOTAL * 100) / ORDENDECOMPRA.VALORTOTAL ),2) PORCENTAJE
                                    FROM ORDENDECOMPRA
                                    INNER JOIN NOVEDADCONTRATO
                                    ON ORDENDECOMPRA.COMPANIA = NOVEDADCONTRATO.COMPANIA
                                    AND ORDENDECOMPRA.CLASEORDEN = NOVEDADCONTRATO.CLASEORDEN
                                    AND ORDENDECOMPRA.NUMERO = NOVEDADCONTRATO.ORDENDECOMPRA
                                    INNER JOIN CLASETRANSACCIONC
