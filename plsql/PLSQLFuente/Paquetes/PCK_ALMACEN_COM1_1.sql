  TIME              : 19:15 PM
  DESCRIPTION       : Valida que los tipos de movimientos 
                      no permita configurar clases de bodegas de SERVICIO con tipos de elementos de CONSUMO y 
                      no permita configurar clases de bodegas de GASTOS con tipos de elementos de DEVOLUTIVOS
  MODIFICATIONS     : 
  PARAMETROS DE ENTRADA: 
    UN_TIPOELEMENTO:          Código de la compañía
    UN_CLASE_BODEGA_DESTINO:   
    UN_CLASE_BODEGA_ORIGEN:
*/
(
  UN_TIPOMOVIMIENTO       IN TIPOMOVIMIENTO.CODIGO%TYPE,
  UN_TIPOELEMENTO         IN TIPOMOVIMIENTO.TIPOELEMENTO%TYPE,
  UN_CLASE_BODEGA_DESTINO IN TIPOMOVIMIENTO.CLASE_BODEGA_DESTINO%TYPE,
  UN_CLASE_BODEGA_ORIGEN  IN TIPOMOVIMIENTO.CLASE_BODEGA_ORIGEN%TYPE
)
AS
  MI_MSGERROR     PCK_SUBTIPOS.TI_CLAVEVALOR; 
  MI_CLASE        VARCHAR2(30);
BEGIN
  IF UN_TIPOELEMENTO='C' AND 30 IN(UN_CLASE_BODEGA_DESTINO, UN_CLASE_BODEGA_ORIGEN)  THEN
    MI_CLASE :='SERVICIO';
    BEGIN
      RAISE PCK_EXCEPCIONES.EXC_ALMACEN;           
    END;  
  END IF;
  IF UN_TIPOELEMENTO<>'C' AND 70 IN(UN_CLASE_BODEGA_DESTINO, UN_CLASE_BODEGA_ORIGEN)  THEN
    MI_CLASE :='GASTO';
    BEGIN
      RAISE PCK_EXCEPCIONES.EXC_ALMACEN;           
    END;  
  END IF;
  EXCEPTION WHEN PCK_EXCEPCIONES.EXC_ALMACEN THEN 
          MI_MSGERROR(1).CLAVE := 'TIPOMOVIMIENTO';
          MI_MSGERROR(1).VALOR := UN_TIPOMOVIMIENTO;
          MI_MSGERROR(2).CLAVE := 'TIPOELEMENTO';
          MI_MSGERROR(2).VALOR := CASE WHEN UN_TIPOELEMENTO='C' THEN 'Consumo' ELSE 'Devolutivo' END;
          MI_MSGERROR(3).CLAVE := 'CLASE_BODEGA';
          MI_MSGERROR(3).VALOR := MI_CLASE;
          PCK_ERR_MSG.RAISE_WITH_MSG(
                 UN_EXC_COD => SQLCODE,
                 UN_TABLAERROR=>'TIPOMOVIMIENTO',
                 UN_ERROR_COD => PCK_ERRORES.ERR_ALMACEN_TIPOMOV_CLASEVALI,
                 UN_REEMPLAZOS => MI_MSGERROR
               );   
