FUNCTION FC_DEPRECIACIONCOMODATO
/*
    NAME              : FC_DEPRECIACIONCOMODATO  --> DEPRECIACIONCOMODATO
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : EDGAR LEONARDO SARMIENTO
    DATE MIGRADOR     : 03/02/2016
    TIME              : 05:46 PM
    SOURCE MODULE     : ALMACEN
    MODIFIER          : JULIO CESAR REINA PANCHE
    DATE MODIFIED     : 13/01/2017
    TIME              : 02:00 PM
    MODIFICATIONS     : CORRECCIÓN SEGÚN EST�?NDAR DE PROGRAMACIÓN Y OPTIMIZACIÓN DEL MANEJO DE ERRORES.
    DESCRIPTION       : Retorna la suma de la depreciación de la tabla acumulado de acuerdo a los parámetros. Se unificó la función DEPRECIACIONCOMODATO y DEPRECIACIONCOMODATOSUM, 
                        mediante el parámetro PAR se especifica cual de estas se ejecuta.
    PARAMETERS        : UN_COMPANIA          => COMPANIA EN LA QUE SE EST�? TRABAJANDO.
                        UN_ELEMENTOINICIAL   => CODIGO INICIAL POR EL CUAL SE VA A FILTRAR LA TABLA ACUMULADO.
                        UN_ELEMENTOFINAL     => CODIGO INICIAL POR EL CUAL SE VA A FILTRAR LA TABLA ACUMULADO.
                        UN_ANIO              => ANIO POR LA CUAL SE VA A FILTRAR LA TABLA ACUMULADO.
                        UN_MES               => MES POR LA CUAL SE VA A FILTRAR LA TABLA ACUMULADO.
                        UN_GRUPO             => CODIGO POR EL CUAL SE VA A FILTRAR LA TABLA ACUMULADO SI UN PAR ES 1
                        UN_PAR               => NUMERO QUE DETERMINA LA FORMA DE CONSULTAR EL TOTAL
    @NAME:    calcularDepreciacionAcumulado 
    @METHOD:  GET
  */

(
	UN_COMPANIA			    IN  PCK_SUBTIPOS.TI_COMPANIA,
	UN_ELEMENTOINICIAL	IN  ACUMULADO.CODIGOELEMENTO%TYPE,
	UN_ELEMENTOFINAL	  IN  ACUMULADO.CODIGOELEMENTO%TYPE,
	UN_ANIO				      IN  PCK_SUBTIPOS.TI_ANIO,
	UN_MES				      IN  PCK_SUBTIPOS.TI_MES,
	UN_GRUPO			      IN  ACUMULADO.CODIGOELEMENTO%TYPE,
	UN_PAR				      IN  PCK_SUBTIPOS.TI_ENTERO
)
RETURN NUMBER
AS
	MI_TOTAL			NUMBER:=0;
BEGIN
    BEGIN
      IF(UN_PAR = 1) THEN
        SELECT NVL(SUM(VLRDEPRECIACIONCOMODATO),0)
        INTO MI_TOTAL
        FROM ACUMULADO
        WHERE COMPANIA                      = UN_COMPANIA
          AND SUBSTR(CODIGOELEMENTO, 1, 3)  = UN_GRUPO
          AND ANO	                          = UN_ANIO
          AND MES	                          = UN_MES
        GROUP BY UN_GRUPO;
      ELSIF(UN_PAR = 2) THEN
        SELECT NVL(SUM(VLRDEPRECIACIONCOMODATO),0)
        INTO MI_TOTAL
        FROM ACUMULADO
        WHERE COMPANIA       = UN_COMPANIA
          AND CODIGOELEMENTO BETWEEN UN_ELEMENTOINICIAL AND UN_ELEMENTOFINAL
          AND ANO	           = UN_ANIO
          AND MES	           = UN_MES;
      END IF;
    EXCEPTION WHEN NO_DATA_FOUND THEN
      MI_TOTAL:=0;
    END;
	RETURN MI_TOTAL;
END FC_DEPRECIACIONCOMODATO; 

PROCEDURE PR_PROMEDIOSENTRADAS
   /*
   NAME 			      : PR_PROMEDIOSENTRADAS
   AUTHORS 			    : SYSMAN SAS
   AUTHOR MIGRACION	: ADRIANA MARITZA C�?CERES BONILLA
   DATE MIGRADOR	  : 04/02/2016
   TIME				      : 02:22 PM
   MODULO ORIGEN	  : ALMACÉN 
   MODIFIER         : JULIO CESAR REINA PANCHE
   DATE MODIFIED    : 13/01/2017
   TIME             : 04:00 PM
   MODIFICATIONS    : CORRECCIÓN SEGÚN EST�?NDAR DE PROGRAMACIÓN Y OPTIMIZACIÓN DEL MANEJO DE ERRORES.
   DESCRIPTION		  : Procedimiento que genera el valor promedio de cada movimiento en la tabla entradas. 
   PARAMETERS       : UN_COMPANIA      => COMPANIA EN LA QUE SE EST�? TRABAJANDO.
                      UN_ELEMENTORECT  => PARAMETRO QUE DETERMINA LA CONSULTA PARA HALLAR EL PROMEDIO DE CADA MOVIMIENTO.
   @NAME:    calcularValorPromedioMovimiento 
   @METHOD:  PUT
  */
(
 UN_COMPANIA           IN  PCK_SUBTIPOS.TI_COMPANIA,
 UN_ELEMENTORECT       IN  D_MOVIMIENTO.ELEMENTO%TYPE
)
AS 
 MI_ACUMULADOCANTIDAD  PCK_SUBTIPOS.TI_DOBLE;
 MI_VALORACUMULADO     PCK_SUBTIPOS.TI_DOBLE;
 MI_PROMEDIOACUMULADO  PCK_SUBTIPOS.TI_DOBLE;
 MI_STRSQL             PCK_SUBTIPOS.TI_STRSQL;
 MI_ELEMENTO           VARCHAR2(200 CHAR);
 MI_CONTADOR           PCK_SUBTIPOS.TI_ENTERO;
 MI_COMPANIADM         VARCHAR2(200 CHAR);
 MI_ELEMENTODM         VARCHAR2(200 CHAR);
 MI_TIPOMOVIMIENTO     D_MOVIMIENTO.TIPOMOVIMIENTO%TYPE;
 MI_MOVIMIENTO         D_MOVIMIENTO.MOVIMIENTO%TYPE;
 MI_CANTIDAD           PCK_SUBTIPOS.TI_DOBLE;
 MI_TOTAL              PCK_SUBTIPOS.TI_DOBLE;
 MI_CODIGO             D_MOVIMIENTO.CODIGO%TYPE;
 MI_TABLA              PCK_SUBTIPOS.TI_TABLA;
 MI_CAMPOS             PCK_SUBTIPOS.TI_CAMPOS;
 MI_CONDICION          PCK_SUBTIPOS.TI_CONDICION;
 MI_RTA                PCK_SUBTIPOS.TI_RTA_ACME;
 MI_RSDMOV             SYS_REFCURSOR;

BEGIN 
EXECUTE IMMEDIATE 'ALTER SESSION SET NLS_NUMERIC_CHARACTERS=''.,''';

 MI_ACUMULADOCANTIDAD  := 0;
 MI_VALORACUMULADO     := 0;
 MI_PROMEDIOACUMULADO  := 0;

 IF UN_ELEMENTORECT = '' THEN 
    MI_STRSQL := 'SELECT COMPANIA,
                         ELEMENTO,
                         TIPOMOVIMIENTO,
                         MOVIMIENTO,
                         CANTIDAD,       
                         TOTAL,
                         CODIGO
                  FROM V_ENTRADAS
                  ORDER BY ELEMENTO, 
                         FECHA,
                         HORA';
 ELSE 
   MI_STRSQL :=  'SELECT DISTINCT D_MOVIMIENTO.COMPANIA, 
                         D_MOVIMIENTO.ELEMENTO,
                         D_MOVIMIENTO.TIPOMOVIMIENTO, 
                         D_MOVIMIENTO.MOVIMIENTO, 
                         D_MOVIMIENTO.CANTIDAD,
                         D_MOVIMIENTO.CANTIDAD*D_MOVIMIENTO.VALORUNITARIO  TOTAL, 
                         D_MOVIMIENTO.CODIGO
                  FROM V_MOVIMIENTO
                    INNER JOIN D_MOVIMIENTO
                      ON V_MOVIMIENTO.COMPANIA        = D_MOVIMIENTO.COMPANIA
                      AND V_MOVIMIENTO.TIPOMOVIMIENTO = D_MOVIMIENTO.TIPOMOVIMIENTO
                      AND V_MOVIMIENTO.NUMERO         = D_MOVIMIENTO.MOVIMIENTO
                    INNER JOIN INVENTARIO 
                      ON D_MOVIMIENTO.COMPANIA  = INVENTARIO.COMPANIA 
                      AND D_MOVIMIENTO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
                  WHERE INVENTARIO.TIPO       = ''C''
                    AND V_MOVIMIENTO.CLASE    = ''E'' 
                    AND D_MOVIMIENTO.IND_REG  NOT IN (0)
                  GROUP BY  D_MOVIMIENTO.COMPANIA, 
                            D_MOVIMIENTO.ELEMENTO, 
                            D_MOVIMIENTO.TIPOMOVIMIENTO,
                            D_MOVIMIENTO.MOVIMIENTO,
                            D_MOVIMIENTO.CANTIDAD,
                            D_MOVIMIENTO.VALORUNITARIO,
                            D_MOVIMIENTO.CODIGO,
                            D_MOVIMIENTO.FECHA
                  ORDER BY D_MOVIMIENTO.ELEMENTO';
 END IF;
 <<ACUMULADO>>
 OPEN MI_RSDMOV FOR MI_STRSQL;
 LOOP
   FETCH MI_RSDMOV INTO MI_COMPANIADM, MI_ELEMENTODM, MI_TIPOMOVIMIENTO, MI_MOVIMIENTO, MI_CANTIDAD, MI_TOTAL, MI_CODIGO;
   EXIT WHEN MI_RSDMOV%NOTFOUND;
        MI_ELEMENTO := NVL(MI_ELEMENTODM, 0);

   IF MI_CANTIDAD <> 0 THEN 
      IF MI_ELEMENTO <> MI_ELEMENTODM THEN 
         MI_ACUMULADOCANTIDAD := 0;
         MI_VALORACUMULADO := 0;
         MI_PROMEDIOACUMULADO := 0;
         MI_ELEMENTO := MI_ELEMENTODM;
      END IF;
         MI_ACUMULADOCANTIDAD := MI_CANTIDAD + MI_ACUMULADOCANTIDAD;
         MI_VALORACUMULADO := MI_TOTAL + MI_VALORACUMULADO;
         MI_PROMEDIOACUMULADO := TO_NUMBER(MI_VALORACUMULADO / MI_ACUMULADOCANTIDAD);

       MI_TABLA := 'D_MOVIMIENTO';
       MI_CAMPOS := 'VLRUNITARIOPROM = ' ||MI_PROMEDIOACUMULADO;
       MI_CONDICION := 'COMPANIA            = '''||UN_COMPANIA||'''
                        AND TIPOMOVIMIENTO  = '''||MI_TIPOMOVIMIENTO||'''
                        AND MOVIMIENTO      = '||MI_MOVIMIENTO||'
                        AND CODIGO          = '||MI_CODIGO||'';
        BEGIN
          BEGIN
            MI_RTA := PCK_DATOS.FC_ACME (UN_TABLA     =>  MI_TABLA, 
                                         UN_ACCION    =>  'M', 
                                         UN_CAMPOS    =>  MI_CAMPOS,
                                         UN_CONDICION =>  MI_CONDICION);                 
            EXCEPTION WHEN PCK_EXCEPCIONES.EXC_ACTUALIZAR THEN
                RAISE PCK_EXCEPCIONES.EXC_ALMACEN;
          END;
            EXCEPTION WHEN PCK_EXCEPCIONES.EXC_ALMACEN THEN    
              PCK_ERR_MSG.RAISE_WITH_MSG(
                UN_EXC_COD   => SQLCODE,
                UN_ERROR_COD => PCK_ERRORES.ERR_ALMACEN_PROMEDIOS_ENTRADAS
              );
        END;
   END IF;
 END LOOP ACUMULADO;
 END PR_PROMEDIOSENTRADAS;

