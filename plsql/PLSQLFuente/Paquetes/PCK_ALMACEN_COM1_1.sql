                        UN_EXC_COD   => SQLCODE,
                        UN_ERROR_COD => PCK_ERRORES.ERR_ALMACEN_ELIM_D_MOVIMIENTO
                      );
      END;
    END IF;
  ELSE
    MI_MSGRETORNO := 'Transacción interrumpida. El header del movimiento no se actualizó correctamente. ';
    RETURN MI_MSGRETORNO;
  END IF;

  MI_MSGRETORNO := 'Proceso finalizado';
  RETURN MI_MSGRETORNO;   
