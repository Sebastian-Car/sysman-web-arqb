FUNCTION FC_ACT_VLR_HIS
/*
  NAME              : FC_ACT_VLR_HIS
  AUTHORS           : SYSMAN
  AUTHOR MIGRACION  :
  DATE MIGRADOR     : 10/05/2024
  TIME              :
  MODIFIER          :CRISTIAN SUESCUN
  REFERENCE         :actualizarVlrSaldo-JAVA
  TICKET            :7742829 
  DATE MODIFIED     :
  TIME              :
  DESCRIPTION       : Evalúa registro por registro para encontrar los que no
                      tengan el Valor Histórico y el saldo por depreciar, para posteriormente agregárselos.
  PARAMETROS DE ENTRADA:
                        UN_COMPANIA
*/
(UN_COMPANIA   IN REEXPRESARVIDAUTIL.COMPANIA%TYPE) -- Parámetro de entrada que indica la compañía a procesar
RETURN VARCHAR2
AS
    -- Declarar variables para almacenar valores de diferentes tablas
    MI_VALOR DEVOLUTIVO.VALOR%TYPE; -- Almacena el valor de un elemento en la tabla DEVOLUTIVO
    MI_NIIF_VLRLIBROS DEPRECIAR.NIIF_VLRLIBROS%TYPE; -- Almacena el valor del libro en la tabla DEPRECIAR
    MI_CONDICION VARCHAR2(4000); -- Almacena la condición para la actualización
    MI_CAMPOS           PCK_SUBTIPOS.TI_CAMPOS; -- Almacena los campos a actualizar
    MI_VALORES          PCK_SUBTIPOS.TI_VALORES; -- No utilizado aquí pero declarado
    MI_RTA              PCK_SUBTIPOS.TI_RTA_ACME; -- Resultado de la actualización

BEGIN
    -- Bucle para recorrer todos los registros de la tabla REEXPRESARVIDAUTIL que no tienen valor histórico ni saldo depreciado
    FOR MI_RS IN (SELECT
                    REEXPRESARVIDAUTIL.COMPANIA,
                    REEXPRESARVIDAUTIL.ELEMENTO,
                    REEXPRESARVIDAUTIL.SERIE,
                    MAX(FECHACAMBIO) AS FECHACAMBIO,
                    CASE WHEN TO_CHAR(MAX(DEPRECIAR.PERIODO), 'MM/YYYY') = TO_CHAR(ADD_MONTHS(MAX(FECHACAMBIO), -1), 'MM/YYYY') THEN
                    TO_CHAR(ADD_MONTHS(MAX(FECHACAMBIO), -1), 'MM/YYYY') ELSE  TO_CHAR( MAX(DEPRECIAR.PERIODO), 'MM/YYYY') END AS FECHAPERIODO
                  FROM REEXPRESARVIDAUTIL
                  JOIN DEPRECIAR ON
                  DEPRECIAR.COMPANIA = REEXPRESARVIDAUTIL.COMPANIA AND
                  DEPRECIAR.ELEMENTO = REEXPRESARVIDAUTIL.ELEMENTO AND 
                  DEPRECIAR.SERIE  = REEXPRESARVIDAUTIL.SERIE
                  WHERE REEXPRESARVIDAUTIL.COMPANIA = UN_COMPANIA -- Solo procesa registros de la compañía dada
                    AND REEXPRESARVIDAUTIL.NIIF_VALOR_TOTAL IS NULL -- Evalúa registros sin valor total
                    AND REEXPRESARVIDAUTIL.NIIF_VLRLIBROS IS NULL
                    
                    GROUP BY REEXPRESARVIDAUTIL.COMPANIA,
                    REEXPRESARVIDAUTIL.ELEMENTO,
                    REEXPRESARVIDAUTIL.SERIE) LOOP -- Evalúa registros sin valor en libros
        
        -- Consulta para obtener los valores históricos y de libros desde las tablas DEVOLUTIVO y DEPRECIAR
        SELECT
            --DEVOLUTIVO.VALOR, -- Obtiene el valor desde la tabla DEVOLUTIVO
            DEPRECIAR.NIIF_VLRELEMENTO,
            DEPRECIAR.NIIF_VLRLIBROS -- Obtiene el valor de los libros desde DEPRECIAR
        INTO
            MI_VALOR, -- Almacena el valor obtenido
            MI_NIIF_VLRLIBROS -- Almacena el valor de los libros
        FROM
            DEVOLUTIVO
        LEFT JOIN
            INVENTARIO ON DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA -- Relaciona con la tabla INVENTARIO por compañía
                      AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO -- Y por elemento
        LEFT JOIN
            DEPRECIAR ON DEVOLUTIVO.COMPANIA = DEPRECIAR.COMPANIA -- Relaciona con DEPRECIAR por compañía
                      AND DEVOLUTIVO.ELEMENTO = DEPRECIAR.ELEMENTO -- Por elemento
                      AND DEVOLUTIVO.SERIE = DEPRECIAR.SERIE -- Y por serie
        WHERE
            DEVOLUTIVO.COMPANIA = UN_COMPANIA -- Filtra por compañía
            AND DEVOLUTIVO.ELEMENTO = MI_RS.ELEMENTO -- Filtra por el elemento actual del bucle
            AND DEVOLUTIVO.SERIE = MI_RS.SERIE -- Filtra por la serie actual del bucle
            AND TO_CHAR(DEPRECIAR.PERIODO, 'MM/YYYY') = MI_RS.FECHAPERIODO; -- Verifica que el periodo coincide con el mes anterior a la fecha de cambio

        -- Preparar los campos que se actualizarán con los valores obtenidos
        MI_CAMPOS := 'NIIF_VALOR_TOTAL = ' ||MI_VALOR || ', NIIF_VLRLIBROS = ' || MI_NIIF_VLRLIBROS;

        -- Construir la condición para actualizar los registros correctos en REEXPRESARVIDAUTIL
        MI_CONDICION := 'COMPANIA            = '''||MI_RS.COMPANIA||'''
                        AND ELEMENTO    = '''||MI_RS.ELEMENTO||'''
                        AND SERIE       = '||MI_RS.SERIE||'
                        AND FECHACAMBIO =
                        TO_DATE('''|| TO_CHAR(MI_RS.FECHACAMBIO , 'DD/MM/YYYY')    || ''',''DD/MM/YYYY HH24:MI:SS '') ';
        
        -- Inicia el proceso de actualización
        BEGIN
            BEGIN
                -- Llama a la función FC_ACME del paquete PCK_DATOS para realizar la actualización
                MI_RTA := PCK_DATOS.FC_ACME (UN_TABLA     =>  'REEXPRESARVIDAUTIL', -- Tabla a actualizar
                                             UN_ACCION    =>  'M', -- Acción de modificar
                                             UN_CAMPOS    =>  MI_CAMPOS, -- Campos a modificar
                                             UN_CONDICION =>  MI_CONDICION); -- Condición de los registros a modificar
            EXCEPTION WHEN PCK_EXCEPCIONES.EXC_ACTUALIZAR THEN
                -- Captura la excepción específica de actualización y la eleva
                RAISE PCK_EXCEPCIONES.EXC_ALMACEN;
            END;
        EXCEPTION WHEN PCK_EXCEPCIONES.EXC_ALMACEN THEN
            -- Captura la excepción específica de almacenamiento y lanza un error con un mensaje
            PCK_ERR_MSG.RAISE_WITH_MSG(
                UN_EXC_COD   => SQLCODE, -- Código de la excepción
                UN_ERROR_COD => PCK_ERRORES.ERR_ALMACEN_PROMEDIOS_ENTRADAS -- Error específico de promedios en entradas
                );
        END;
    END LOOP;

    -- Si todo el proceso se realiza correctamente, retorna un mensaje de éxito
    RETURN 'ACTUALIZACION REALIZADA CORRECTAMENTE ';

    -- Captura cualquier otra excepción no manejada y retorna un mensaje de error
    EXCEPTION
        WHEN OTHERS THEN
            RETURN 'ERROR AL ACTUALIZAR EL VALOR HISTORICO';
END FC_ACT_VLR_HIS;

PROCEDURE PR_ELIMINAR_MOVIMIENTOS
   /*
   NAME 			      : PR_ELIMINAR_MOVIMIENTOS
   AUTHORS 			    : LINA PAOLA VEGA
   DATE MIGRADOR	  : 03/10/2024
   MODULO ORIGEN	  : ALMACÉN 
   TIME             : 04:00 PM
   @NAME:    eliminarMovimientos 
   @METHOD:  DELETE
  */
(
    UN_COMPANIA        IN PCK_SUBTIPOS.TI_COMPANIA,
    UN_ANIO			   IN  PCK_SUBTIPOS.TI_ANIO,
    UN_TIPOMOVIMIENTO  IN D_MOVIMIENTO.TIPOMOVIMIENTO%TYPE,
    UN_MOVIMIENTO      IN D_MOVIMIENTO.MOVIMIENTO%TYPE  
)
AS 
    MI_TIPOMOVIMIENTO   D_MOVIMIENTO.TIPOMOVIMIENTO%TYPE;
    MI_MOVIMIENTO       D_MOVIMIENTO.MOVIMIENTO%TYPE; 
    MI_RTA              PCK_SUBTIPOS.TI_RTA_ACME;
    MI_ELEM_INI         DEVOLUTIVO.ELEMENTO%TYPE;
    MI_ELEM_FIN         DEVOLUTIVO.ELEMENTO%TYPE;
    MI_PLACA_INI        DEVOLUTIVO.SERIE%TYPE;
    MI_PLACA_FIN        DEVOLUTIVO.SERIE%TYPE;
    MI_MES              VARCHAR2(15);
    MI_ANIO             VARCHAR2(15);
    

  
    BEGIN
        BEGIN
            SELECT MIN(ELEMENTO), MAX(ELEMENTO), MIN(SERIE), MAX(SERIE), TO_CHAR(FECHA, 'YYYY'),TO_CHAR(FECHA, 'MM')
              INTO MI_ELEM_INI, MI_ELEM_FIN, MI_PLACA_INI, MI_PLACA_FIN, MI_ANIO, MI_MES
              FROM D_MOVIMIENTO
              WHERE COMPANIA = UN_COMPANIA
              AND TIPOMOVIMIENTO = UN_TIPOMOVIMIENTO
              AND MOVIMIENTO = UN_MOVIMIENTO
              GROUP BY TO_CHAR(FECHA, 'YYYY'), TO_CHAR(FECHA, 'MM');
        EXCEPTION WHEN NO_DATA_FOUND THEN
          RETURN;
        END;
       
        FOR RS IN(
            SELECT *
            FROM D_MOVIMIENTO WHERE
            COMPANIA = UN_COMPANIA
            AND TIPOMOVIMIENTO = UN_TIPOMOVIMIENTO
            AND MOVIMIENTO = UN_MOVIMIENTO            
            )
  LOOP           
        DELETE FROM  D_MOVIMIENTO WHERE COMPANIA     = UN_COMPANIA  AND TIPOMOVIMIENTO       = UN_TIPOMOVIMIENTO   AND MOVIMIENTO = UN_MOVIMIENTO;
          COMMIT;
        
        UPDATE  DEVOLUTIVO D SET PLACAANULADA = 0, FECHAANULADA = NULL
        WHERE EXISTS (
                SELECT 1
                FROM D_MOVIMIENTO DM
                WHERE DM.COMPANIA = D.COMPANIA
                AND DM.ELEMENTO = D.ELEMENTO
                AND DM.SERIE = D.SERIE
                AND DM.TIPOMOVIMIENTO = D.TIPOMOVIMIENTOI
                AND DM.MOVIMIENTO = D.MOVIMIENTOI
                AND DM.MOVIMIENTO = UN_MOVIMIENTO
                AND DM.TIPOMOVIMIENTO = UN_TIPOMOVIMIENTO)
        ;
        
  END LOOP;
          PCK_ALMACEN_COM3.PR_REVISAHORAH(  UN_COMPANIA =>  UN_COMPANIA);
          
           MI_RTA:= PCK_ALMACEN_COM3.FC_REVISAHORAS(
                        UN_COMPANIA       => UN_COMPANIA,
                        UN_ELEMENTOINI          => MI_ELEM_INI,
                        UN_ELEMENTOFIN           => MI_ELEM_FIN,
                        UN_PLACAINICIAL       => MI_PLACA_INI,
                        UN_PLACAFINAL          => MI_PLACA_FIN);
                        
          MI_RTA:= PCK_ALMACEN_COM3.FC_REVISADEVOLUTIVOSH(
            UN_COMPANIA        => UN_COMPANIA,
            UN_ANIOINICIAL     => MI_ANIO,
            UN_MESINICIAL      => MI_MES,
            UN_ELEMENTOINICIAL => MI_ELEM_INI,
            UN_SERIEINICIAL    => MI_PLACA_INI,
            UN_ANIOFINAL       => MI_ANIO,
            UN_MESFINAL        => MI_MES,
            UN_ELEMENTOFINAL   => MI_ELEM_FIN,
            UN_SERIEFINAL      => MI_PLACA_INI,
            UN_USUARIO         => NULL);
            
          PCK_ALMACEN_COM3.PR_ACTUALIZADEPENDENCIA(
            UN_COMPANIA        => UN_COMPANIA,
            UN_ELEMENTOINICIAL => MI_ELEM_INI,
            UN_ELEMENTOFINAL    => MI_ELEM_FIN,
            UN_PLACAINICIAL   => MI_PLACA_INI,
            UN_PLACAFINAL      => MI_PLACA_FIN,
            UN_MESFINAL      => MI_MES,
            UN_ANIOFINAL     => MI_ANIO);
            
          PCK_ALMACEN_COM3.PR_RECTIFICARDEVOLUTIVOS(
            UN_COMPANIA        => UN_COMPANIA,
            UN_PLACAINICIAL   => MI_PLACA_INI,
            UN_PLACAFINAL      => MI_PLACA_FIN);

END PR_ELIMINAR_MOVIMIENTOS;

