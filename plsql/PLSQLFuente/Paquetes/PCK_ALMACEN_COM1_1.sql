  NAME              : FC_GENERAR_CONSECUTIVOALMACEN
  AUTHORS           : SYSMAN
  AUTHOR MIGRACION  : 
  DATE MIGRADOR     : 
  TIME              : 
  MODIFIER          : JOSE PASCUAL GOMEZ BLANCO
