FUNCTION FC_CARGAR_REEXP_VID_UTL
/*
  NAME              : FC_CARGAR_REEXP_VID_UTL
  AUTHORS           : SYSMAN
  AUTHOR MIGRACION  : 
  DATE MIGRADOR     : 8/05/2024
  TIME              : 
  MODIFIER          : 
  DATE MODIFIED     : 
  TIME              :
  DESCRIPTION       : Carga los datos de reexpresarVidaUtuil ingresados desde una plantilla 
  MODIFICATIONS     : 
  PARAMETROS DE ENTRADA: 
      UN_COMPANIA   
    , UN_ELEMENTO   
    , UN_SERIE      
    , UN_FECHA      
    , UN_VIDA_UTIL  
    , UN_USUARIO 
*/
(
  UN_COMPANIA   IN REEXPRESARVIDAUTIL.COMPANIA%TYPE
, UN_ELEMENTO   IN  REEXPRESARVIDAUTIL.ELEMENTO%TYPE
, UN_SERIE      IN REEXPRESARVIDAUTIL.SERIE%TYPE
, UN_FECHA      IN REEXPRESARVIDAUTIL.FECHACAMBIO%TYPE
, UN_VIDA_UTIL  IN REEXPRESARVIDAUTIL.VIDAUTIL%TYPE
, UN_USUARIO    IN VARCHAR2
) RETURN VARCHAR2
AS 
  MI_ELEMENTO NUMBER;
  MI_NOM_ELEMENTO REEXPRESARVIDAUTIL.NOMBRELARGO%TYPE;
  MI_SERIE NUMBER;
  MI_REEXPRESAR NUMBER;
  MI_FECHA DATE;
  MI_CAMPOS           PCK_SUBTIPOS.TI_CAMPOS;
  MI_VALORES          PCK_SUBTIPOS.TI_VALORES;
  MI_RTA_ACME         PCK_SUBTIPOS.TI_RTA_ACME;
  MI_EXIS_REGTRO NUMBER;
  
BEGIN
    BEGIN 
        IF UN_ELEMENTO IS NULL THEN RETURN 'El elemento no puede estar vacío'; END IF;
        IF UN_SERIE IS NULL  THEN RETURN 'El elemento no puede estar vacío'; END IF;
        IF UN_FECHA IS NULL THEN RETURN 'El elemento no puede estar vacío'; END IF;
        
        
        -- Usando TRUNC para comparar solo la parte de la fecha
        SELECT COUNT(*) INTO MI_EXIS_REGTRO
        FROM REEXPRESARVIDAUTIL
        WHERE 
        COMPANIA = UN_COMPANIA
        AND SERIE = UN_SERIE
        AND TRUNC(FECHACAMBIO) = TRUNC(UN_FECHA);
        IF MI_EXIS_REGTRO > 0 THEN RETURN'Registro ya existente en Reexpresar'; END IF;
        
        
