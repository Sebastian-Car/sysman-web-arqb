  FUNCTION FC_CALACUMULADO
/*
    NAME              : FC_CALACUMULADO  --> calAcumulado
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : YESIKA PAOLA BECERRA CASTRO
    DATE MIGRADOR     : 04/02/2016
    TIME              : 05:30 PM
    SOURCE MODULE     : ALMACEN
    MODIFIER          : JULIO CESAR REINA PANCHE
    DATE MODIFIED     : 13/01/2017
    TIME              : 10:00 AM
    MODIFICATIONS     : CORRECCIÓN SEGÚN EST�?NDAR DE PROGRAMACIÓN Y OPTIMIZACIÓN DEL MANEJO DE ERRORES.
    DESCRIPTION       : Calcula el Acumulado en cantidades o valores de los elementos de Inventario para la Tarjeta Kardex
    PARAMETERS        : UN_COMPANIA   => COMPANIA EN LA QUE SE EST�? TRABAJANDO.
                        UN_ELEMENTO   => CODIGO POR EL CUAL SE VA A FILTRAR LA TABLA D_MOVIMIENTO.
                        UN_FECHA      => FECHA POR EL CUAL SE VA A FILTRAR TABLA D_MOVIMIENTO.
                        UN_OPCION     => OPCION POR LA CUAL SE VA A CALCULAR EL ACUMULADO.
                        UN_TIPOKARDEX => CODIGO FINAL POR EL CUAL SE VA A FILTRAR EL INVENTARIO.
                        UN_SERIE      => CADENA A AGREGAR.
      @NAME:    calcularAcumuladoInventario 
      @METHOD:  GET
  */

( 
  UN_COMPANIA 		    IN  PCK_SUBTIPOS.TI_COMPANIA,
	UN_ELEMENTO   	    IN  D_MOVIMIENTO.ELEMENTO%TYPE, 
	UN_FECHA 		        IN  D_MOVIMIENTO.FECHA%TYPE, 
	UN_OPCION 			    IN  PCK_SUBTIPOS.TI_ENTERO,
	UN_TIPOKARDEX 		  IN  PCK_SUBTIPOS.TI_ENTERO, 
	UN_SERIE            IN  D_MOVIMIENTO.SERIE%TYPE
)
RETURN NUMBER
  AS
      MI_TIPOMOVIMIENTO 	VARCHAR2(1000);
      MI_CALACUMULADO 	  VARCHAR2(32000);
      MI_SALDOKARDEX      D_MOVIMIENTO.SALDOKARDEX%TYPE;
      MI_SALDO            D_MOVIMIENTO.VALORSALDO%TYPE;
      MI_VLRAJUSTADO      D_MOVIMIENTO.VLRAJUSTADO%TYPE;
      MI_VLRUNIT          D_MOVIMIENTO.VALORUNITARIO%TYPE;
      MI_UTIPOMOVIMIENTO  D_MOVIMIENTO.TIPOMOVIMIENTO%TYPE;
      MI_CLASE            V_MOVIMIENTO.CLASE%TYPE;
      MI_CONCEPTO         V_MOVIMIENTO.CONCEPTO%TYPE;
      MI_VALORTOTAL       D_MOVIMIENTO.VALORTOTAL%TYPE;
BEGIN 
  IF UN_TIPOKARDEX IS NOT NULL THEN 
  BEGIN
    SELECT   SALDOKARDEX,
             VLRAJUSTADO,
             VALORSALDO,  
             UTIPOMOVIMIENTO, 
             CLASE, 
             VALORUNITARIO, 
             VALORTOTAL,  
             CONCEPTO 
    INTO    MI_SALDOKARDEX, 
            MI_VLRAJUSTADO,
            MI_SALDO, 
            MI_UTIPOMOVIMIENTO, 
            MI_CLASE, 
            MI_VLRUNIT, 
            MI_VALORTOTAL, 
            MI_CONCEPTO
    FROM (
      SELECT  D_MOVIMIENTO.SALDOKARDEX,
              D_MOVIMIENTO.VLRAJUSTADO,
              D_MOVIMIENTO.VALORSALDO,  
              D_MOVIMIENTO.TIPOMOVIMIENTO AS UTIPOMOVIMIENTO, 
              V_MOVIMIENTO.CLASE , 
              D_MOVIMIENTO.VALORUNITARIO, 
              D_MOVIMIENTO.VALORTOTAL,  
              V_MOVIMIENTO.CONCEPTO
      FROM    D_MOVIMIENTO 
        INNER JOIN V_MOVIMIENTO
          ON V_MOVIMIENTO.COMPANIA        = D_MOVIMIENTO.COMPANIA
          AND V_MOVIMIENTO.TIPOMOVIMIENTO = D_MOVIMIENTO.TIPOMOVIMIENTO
          AND V_MOVIMIENTO.NUMERO         = D_MOVIMIENTO.MOVIMIENTO
      WHERE   D_MOVIMIENTO.COMPANIA    	=  UN_COMPANIA
        AND 	D_MOVIMIENTO.ELEMENTO 		=  UN_ELEMENTO 
        AND 	D_MOVIMIENTO.FECHA   			<= UN_FECHA
        AND   D_MOVIMIENTO.SERIE        =  UN_SERIE
        AND 	D_MOVIMIENTO.IND_REG 			<> 0  
      ORDER BY D_MOVIMIENTO.FECHA DESC, D_MOVIMIENTO.HORA DESC)
    WHERE ROWNUM = 1;

    EXCEPTION WHEN NO_DATA_FOUND THEN 
        RAISE PCK_EXCEPCIONES.EXC_ALMACEN;

    END;

    CASE UN_TIPOKARDEX
      WHEN 1 
      THEN 
        IF UN_OPCION < 10 THEN
          IF MI_CALACUMULADO <> 0 THEN
            IF UN_OPCION = 1 THEN 
              MI_CALACUMULADO:=  MI_SALDOKARDEX;
            ELSIF UN_OPCION = 2 THEN 
              MI_CALACUMULADO:=  MI_SALDO;
            ELSIF UN_OPCION = 3 THEN 
              MI_CALACUMULADO:=  MI_VLRAJUSTADO ;
            ELSIF UN_OPCION = 4 THEN 
              MI_CALACUMULADO:= MI_VLRAJUSTADO * MI_SALDOKARDEX;
            END IF;
          END IF;
          ELSE
            IF UN_OPCION = 10 THEN 
              MI_CALACUMULADO:= MI_SALDOKARDEX;
            ELSIF UN_OPCION = 20 THEN 
              MI_CALACUMULADO:= MI_SALDO;
            END IF;
        END IF;
      WHEN 2 
      THEN 
        IF UN_OPCION < 10 THEN 
          IF MI_CLASE = 'E' THEN
            IF UN_OPCION = 1 THEN 
              MI_CALACUMULADO := 1;
            ELSIF UN_OPCION = 2 THEN 
              MI_CALACUMULADO := MI_VLRUNIT;
            END IF;
          ELSE
            IF UN_OPCION = 1 THEN 
              MI_CALACUMULADO := 0;
            ELSIF UN_OPCION = 2 THEN 
              MI_CALACUMULADO := 0;
            END IF;
          END IF;
        ELSE 
          IF UN_OPCION = 10 THEN 
            MI_CALACUMULADO:= MI_SALDOKARDEX;
          ELSIF UN_OPCION = 20 THEN 
            MI_CALACUMULADO:= MI_SALDO;
          END IF;
        END IF;
      WHEN 3 
      THEN 
        IF UN_OPCION < 10 THEN
          IF MI_VALORTOTAL <> 0 AND MI_UTIPOMOVIMIENTO <> 0 THEN 
            MI_TIPOMOVIMIENTO:= MI_UTIPOMOVIMIENTO;
          ELSE 
            MI_CALACUMULADO:= 0;
          END IF;

          IF MI_CONCEPTO <> '' AND MI_CLASE <> '' THEN 
            IF MI_CLASE = 'E' THEN 
              IF UN_OPCION = 1 THEN 
                MI_CALACUMULADO:= 0; 
              ELSIF UN_OPCION = 2 THEN
                MI_CALACUMULADO:= 0; 
              END IF;
            ELSE 
              IF MI_CLASE = 'S' AND MI_CONCEPTO = 'N' THEN 
                IF UN_OPCION = 1 THEN 
                  MI_CALACUMULADO:= 1; 
                ELSIF  UN_OPCION = 2 THEN
                  MI_CALACUMULADO:= MI_VALORTOTAL; 
                END IF;
              ELSE 
                IF MI_CLASE = 'T' THEN 
                  IF MI_CONCEPTO = 'T' THEN 
                    MI_CALACUMULADO:= 0;
                  END IF;
                ELSE 
                  MI_CALACUMULADO:= 0;
                END IF;
              END IF;
            END IF;
          END IF; 
        END IF;
    END CASE;

    IF MI_SALDOKARDEX <> 0 AND  MI_VLRUNIT <> 0 THEN 
      IF UN_OPCION = 1 THEN 
        MI_CALACUMULADO:= MI_SALDOKARDEX;
      ELSIF UN_OPCION = 2 THEN 
        MI_CALACUMULADO:= MI_VLRUNIT;
      END IF;
    ELSE 
      MI_CALACUMULADO:= 0;
    END IF;
  END IF;

  RETURN MI_CALACUMULADO;  

  EXCEPTION WHEN PCK_EXCEPCIONES.EXC_ALMACEN THEN    
      PCK_ERR_MSG.RAISE_WITH_MSG(
        UN_EXC_COD =>SQLCODE,
        UN_ERROR_COD=>PCK_ERRORES.ERR_ALMACEN_CAL_ACUMULADO
      );
   --EXCEPTION WHEN OTHERS THEN
    --PCK_DATOS.GL_ERROR_MSG := 'Error al hallar predecesor';
    --PCK_DATOS.GL_ERROR_MSG := PCK_SYSMAN_UTL.FC_EVALUAR_ERROR(MI_ERROR_FUN, PCK_DATOS.GL_ERROR_MSG,  '','',SQLERRM );
    --RAISE_APPLICATION_ERROR (-20000, PCK_DATOS.GL_ERROR_MSG || CHR(10) || PCK_DATOS.GL_ERROR_MSG );
END FC_CALACUMULADO;

