          AND EGR.TIPO_CPTE_AFECT IS NOT NULL   
        
        UNION ALL

        SELECT DC.COMPANIA,
               DC.ANO,
               DC.TIPO_CPTE,
               DC.COMPROBANTE,
               DC.CONSECUTIVO,
               CASE WHEN AF.COMPANIA IS NULL OR TC.CLASE_CONTABLE NOT IN ('U') 
                  THEN DC.DEBITO_AFECTADO ELSE 0 END AS DEBITO_AFECTADO,
               CASE WHEN AF.COMPANIA IS NULL OR TC.CLASE_CONTABLE = 'U' THEN DC.CREDITO_AFECTADO
                  ELSE 0 END AS CREDITO_AFECTADO,
               SUM(CASE WHEN AF.COMPANIA IS NULL OR TC.CLASE_CONTABLE NOT IN ('U') 
                  THEN NVL(AF.VALOR_DEBITO, 0) + NVL(AF.VALOR_CREDITO, 0)
                  ELSE 0 END) AS DEBITO_AFECTADO_CAL,
               SUM(CASE WHEN TC.CLASE_CONTABLE = 'U' THEN NVL(AF.VALOR_DEBITO, 0) + NVL(AF.VALOR_CREDITO, 0)
                   ELSE 0 END) AS CREDITO_AFECTADO_CAL
        FROM DETALLE_COMPROBANTE_CNT DC
        LEFT JOIN DETALLE_COMPROBANTE_CNT AF
            ON DC.COMPANIA = AF.COMPANIA
            AND DC.ANO = AF.ANO_AFECT
            AND DC.TIPO_CPTE = AF.TIPO_CPTE_AFECT
