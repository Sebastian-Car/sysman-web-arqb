      TIME              :
      DESCRIPTION       : Procedimiento que actualiza la información de Control Cartera para el siguiente año
      MODIFICATIONS     :
      --NAME: actualizarAnoControlCartera
    */
  (
  UN_COMPANIA     IN PCK_SUBTIPOS.TI_COMPANIA,
  UN_ANIOFUENTE   IN PCK_SUBTIPOS.TI_ENTERO,
  UN_ANIODESTINO  IN PCK_SUBTIPOS.TI_ENTERO,
  UN_USUARIO      IN PCK_SUBTIPOS.TI_USUARIO
  )
  AS
  MI_CONTADOR      NUMBER(5,0);
  MI_CAMPOS        PCK_SUBTIPOS.TI_CAMPOS;
  MI_VALORES       PCK_SUBTIPOS.TI_VALORES;
  MI_MSGERROR      PCK_SUBTIPOS.TI_CLAVEVALOR;
BEGIN
  SELECT COUNT(*)
  INTO MI_CONTADOR
  FROM CARTERA_CUENTA
  WHERE COMPANIA = UN_COMPANIA
    AND ANO      = UN_ANIODESTINO;
 BEGIN
  IF MI_CONTADOR > 0 THEN
    RAISE PCK_EXCEPCIONES.EXC_CONTABILIDAD;
  ELSE
       MI_CAMPOS := '  COMPANIA
                     , ANO
                     , TIPO_COMPROBANTE
                     , CUENTA
                     , MODULO
                     , IND_CAUSACION
                     , CREATED_BY
                     , DATE_CREATED';
    MI_VALORES := 'SELECT COMPANIA
                     , '||UN_ANIODESTINO||'
                     , TIPO_COMPROBANTE
                     , CUENTA
                     , MODULO
                     , IND_CAUSACION
                     ,'''||UN_USUARIO||'''
                     , SYSDATE
             FROM   CARTERA_CUENTA
             WHERE  COMPANIA = '''||UN_COMPANIA||'''
               AND  ANO      = '||UN_ANIOFUENTE||'  ';
    BEGIN
       BEGIN
         PCK_DATOS.GL_RTA      := PCK_DATOS.FC_ACME(UN_TABLA       =>'CARTERA_CUENTA',
                                                    UN_ACCION      =>'IS',
                                                    UN_CAMPOS  =>MI_CAMPOS,
                                                    UN_VALORES =>MI_VALORES
                                                    );
            EXCEPTION WHEN  PCK_EXCEPCIONES.EXC_INSERTAR THEN
                       RAISE PCK_EXCEPCIONES.EXC_CONTABILIDAD;
            END;
                EXCEPTION
                     WHEN  PCK_EXCEPCIONES.EXC_PLANDES THEN
                        MI_MSGERROR(1).CLAVE := 'ANIO';
                        MI_MSGERROR(1).VALOR := UN_ANIODESTINO;
                      PCK_ERR_MSG.RAISE_WITH_MSG(
                                              UN_EXC_COD =>SQLCODE
                                              ,UN_ERROR_COD=>PCK_ERRORES.ER_CONTAB_INSECONTROLCASRTERA
                                              ,UN_REEMPLAZOS  => MI_MSGERROR
                                              );
      END;
  END IF;
     EXCEPTION WHEN PCK_EXCEPCIONES.EXC_CONTABILIDAD THEN
          MI_MSGERROR(1).CLAVE := 'ANIO';
          MI_MSGERROR(1).VALOR := UN_ANIODESTINO;
          PCK_ERR_MSG.RAISE_WITH_MSG(
                      UN_EXC_COD =>SQLCODE
                      ,UN_ERROR_COD=>PCK_ERRORES.ER_CONTAB_INSECONTROLCASRTERA
                      ,UN_REEMPLAZOS  => MI_MSGERROR  );
 END;
END PR_ACTUANOCONTROLCARTERA;

PROCEDURE PR_EST_CAMBIOS_PATRI
 (
    UN_COMPANIA        IN PCK_SUBTIPOS.TI_COMPANIA,
    UN_ANIO_INI        IN PCK_SUBTIPOS.TI_ANIO,
    UN_ANIO_FIN        IN PCK_SUBTIPOS.TI_ANIO,
    UN_MES_INI         IN PCK_SUBTIPOS.TI_MES,
    UN_MES_FIN         IN PCK_SUBTIPOS.TI_MES,
    UN_CODIGO_INI      IN PLAN_CONTABLE.CODIGO%TYPE,
    UN_CODIGO_FIN      IN PLAN_CONTABLE.CODIGO%TYPE
  )AS
  MI_CAMPOS             PCK_SUBTIPOS.TI_CAMPOS;
  MI_VALORES            PCK_SUBTIPOS.TI_VALORES;
  MI_ORDEN              NUMBER := 0;
  MI_STRSQL             CLOB;
