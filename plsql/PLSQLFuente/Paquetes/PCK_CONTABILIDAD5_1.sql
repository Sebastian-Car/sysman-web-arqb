  PROCEDURE PR_CREARDETALLESPPTALESVARIOS 
  /*
    NAME              : PR_CREARDETALLESPPTALESVARIOS
    AUTHORS           : SYSMAN LTDA
    AUTHOR MIGRACION  : JUAN CARLOS RODR�?GUEZA AMÉZQUITA
    DATE MIGRADOR     : 12/12/2016 
    TIME              : 03:41 PM     
    MODIFIER          : 
    DATE MODIFIED     : 24/01/2017
    TIME MODIFIED     : 03:12 PM
    DESCRIPTION       : Inserta los rubros presupuestales de ING, ICA o NBA que 
                        se crean desde contabilidad.
    MODIFICATIONS     : 
    PARAMETROS DE ENTRADA:
      UN_COMPANIA:    Código de la compañía
      UN_ANO:         Año en el cual se realiza el comprobante
      UN_TIPO:        Tipo de comprobante contable
      UN_NUMERO:      Número de comprobante según el tipo
      UN_CLASE:       Clase del tipo de comprobante presupuestal

    @NAME: crearDetallesPptalesVarios
    @METHOD: post
    */
  (
    UN_COMPANIA               IN PCK_SUBTIPOS.TI_COMPANIA
  , UN_ANO                    IN PCK_SUBTIPOS.TI_ANIO
  , UN_TIPO                   IN PCK_SUBTIPOS.TI_TIPOCOMPROBANTE
  , UN_NUMERO                 IN PCK_SUBTIPOS.TI_NUMEROCOMPROBANTECNT
  , UN_CLASE                  IN PCK_SUBTIPOS.TI_CLASECUENTACONTA
  , UN_CADENAINSERTAR         IN PCK_SUBTIPOS.TI_VALORES
  ) AS 
    -- Detalles en los que las cuentas tengan asociado un rubro presupuestal
    CURSOR MI_CURSOR
    IS
      SELECT PLAN_CONTABLE.CLASECUENTA,
             PLAN_CONTABLE.CUENTA_PPTAL,
             DETALLE_COMPROBANTE_CNT.VALOR_DEBITO,
             DETALLE_COMPROBANTE_CNT.EJECUCION_DEBITO,
             DETALLE_COMPROBANTE_CNT.EJECUCION_CREDITO,
             DETALLE_COMPROBANTE_CNT.VALOR_CREDITO,
             DETALLE_COMPROBANTE_CNT.CENTRO_COSTO,
             DETALLE_COMPROBANTE_CNT.AUXILIAR,
             DETALLE_COMPROBANTE_CNT.CONSECUTIVO,
             DETALLE_COMPROBANTE_CNT.CUENTA,
             DETALLE_COMPROBANTE_CNT.TERCERO,
             DETALLE_COMPROBANTE_CNT.SUCURSAL,
             DETALLE_COMPROBANTE_CNT.RECONOCIMIENTO
        FROM DETALLE_COMPROBANTE_CNT
             INNER JOIN PLAN_CONTABLE 
             ON  (DETALLE_COMPROBANTE_CNT.CUENTA = PLAN_CONTABLE.CODIGO)
             AND (DETALLE_COMPROBANTE_CNT.ANO = PLAN_CONTABLE.ANO)
             AND (DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA)
             INNER JOIN PLAN_PPTAL_CUENTACNT
             ON  (PLAN_CONTABLE.COMPANIA = PLAN_PPTAL_CUENTACNT.COMPANIA)
             AND (PLAN_CONTABLE.ANO = PLAN_PPTAL_CUENTACNT.ANO)
             AND (PLAN_CONTABLE.CODIGO = PLAN_PPTAL_CUENTACNT.CUENTA_CONTABLE)
       WHERE DETALLE_COMPROBANTE_CNT.COMPANIA = UN_COMPANIA
         AND DETALLE_COMPROBANTE_CNT.ANO = UN_ANO
         AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = UN_TIPO
         AND DETALLE_COMPROBANTE_CNT.COMPROBANTE = UN_NUMERO
         AND (VALOR_CREDITO <> 0 OR VALOR_DEBITO <> 0)
       ORDER BY DETALLE_COMPROBANTE_CNT.COMPROBANTE;
    -- Datos del comprobante contable
    MI_FECHA                        DATE;
    MI_DESCRIPCION                  DETALLE_COMPROBANTE_CNT.DESCRIPCION%TYPE;
    --    
    MI_PARAMETRO                    VARCHAR2(255 CHAR);
    MI_VALOR_DEBITO                 DETALLE_COMPROBANTE_CNT.VALOR_DEBITO%TYPE;
    MI_VALOR_CREDITO                DETALLE_COMPROBANTE_CNT.VALOR_CREDITO%TYPE;
    MI_RUBRO_PPTAL                  PLAN_CONTABLE.CUENTA_PPTAL%TYPE;
    -- Variables ACME    
    MI_CAMPOS                       PCK_SUBTIPOS.TI_CAMPOS;
    MI_VALORES                      PCK_SUBTIPOS.TI_VALORES;
    MI_CONDICION                    PCK_SUBTIPOS.TI_CONDICION;
    MI_PCKDATOS                     PCK_SUBTIPOS.TI_RTA_ACME;
  BEGIN
    -- Extracción de datos del comprobante contable
    BEGIN
      SELECT FECHA, DESCRIPCION
        INTO MI_FECHA, MI_DESCRIPCION
        FROM COMPROBANTE_CNT
       WHERE COMPANIA = UN_COMPANIA
         AND ANO = UN_ANO
         AND TIPO = UN_TIPO
         AND NUMERO = UN_NUMERO;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        RAISE PCK_EXCEPCIONES.EXC_CONTABILIDAD;
      WHEN PCK_EXCEPCIONES.EXC_CONTABILIDAD THEN
        PCK_ERR_MSG.RAISE_WITH_MSG(
          UN_EXC_COD => SQLCODE,
          UN_ERROR_COD => PCK_ERRORES.ERR_CONTAB_GENCPTEPPTALVARIOS1
        );
    END;
    --
    <<procesar_detalle_contable>>
    FOR RS
    IN  MI_CURSOR
    LOOP
      -- Verifica si la cuenta ya tiene un rubro asociado en el detalle contable.
      IF RS.CUENTA_PPTAL IS NULL THEN
        BEGIN
          SELECT RUBRO
            INTO MI_RUBRO_PPTAL
            FROM PLAN_PPTAL_CUENTACNT
           WHERE COMPANIA        = UN_COMPANIA
             AND ANO             = UN_ANO
             AND CUENTA_CONTABLE = RS.CUENTA;
         EXCEPTION
          WHEN TOO_MANY_ROWS THEN
            RAISE PCK_EXCEPCIONES.EXC_CONTABILIDAD;
          WHEN PCK_EXCEPCIONES.EXC_CONTABILIDAD THEN
            PCK_ERR_MSG.RAISE_WITH_MSG(
              UN_EXC_COD => SQLCODE,
              UN_ERROR_COD => PCK_ERRORES.ERR_CONTAB_VARIASEQUIVALENCIAS
            );
        END;
      ELSE
        MI_RUBRO_PPTAL := RS.CUENTA_PPTAL;
      END IF;
      -- Actualización de cuenta y comprobante presupuestal afectado
      BEGIN
        MI_CAMPOS := 'CUENTAPPTAL = ''' || MI_RUBRO_PPTAL || '''
                    , TIPOPPTAL = ''' || UN_TIPO || '''
                    , NUMEROPPTAL = ' || UN_NUMERO || '
                    , CONSECUTIVOPPTO = ' || RS.CONSECUTIVO;
        MI_CONDICION := 'COMPANIA = ''' || UN_COMPANIA || ''' 
                         AND ANO = ' || UN_ANO || ' 
                         AND TIPO_CPTE = ''' || UN_TIPO || ''' 
                         AND COMPROBANTE = ' || UN_NUMERO || ' 
                         AND CONSECUTIVO = ' || RS.CONSECUTIVO;
        MI_PCKDATOS := PCK_DATOS.FC_ACME(
          UN_TABLA => 'DETALLE_COMPROBANTE_CNT', 
          UN_ACCION => 'M', 
          UN_CAMPOS => MI_CAMPOS, 
          UN_CONDICION => MI_CONDICION
        );
      EXCEPTION
        WHEN PCK_EXCEPCIONES.EXC_ACTUALIZAR THEN
          PCK_ERR_MSG.RAISE_WITH_MSG(
            UN_EXC_COD => SQLCODE,
            UN_TABLAERROR => 'DETALLE_COMPROBANTE_CNT',
            UN_ERROR_COD => PCK_ERRORES.ERR_CONTAB_ACTPPTO_DETCONTABLE
          ); 
      END;
      -- Débito y crédito
      MI_PARAMETRO := PCK_SYSMAN_UTL.FC_PAR(UN_COMPANIA, 'INGRESO PRESUPUESTAL DESDE ORDEN DE PAGO', PCK_DATOS.MODULOCONTABILIDAD , SYSDATE);
      IF MI_PARAMETRO = 'SI' THEN
          MI_VALOR_DEBITO := RS.VALOR_DEBITO;
          MI_VALOR_CREDITO := RS.VALOR_CREDITO;
      ELSE
          MI_VALOR_DEBITO := RS.EJECUCION_DEBITO;
