                    TO_CHAR(V_ANO - 1) || ''', '''                      ||
                    V_CAPITAL    || ''', ''' || V_RESERVAS   || ''', '''      ||
                    V_RESULTADO  || ''', ''' || V_RESULT_ANT || ''', '''     ||
                    V_OTRAS || ''' ';
  BEGIN
      BEGIN
          PCK_DATOS.GL_RTA := PCK_DATOS.FC_ACME(
              UN_TABLA   => 'TEMP_ESTADOS_PATRIMONIO',
              UN_ACCION  => 'I',
              UN_CAMPOS  => MI_CAMPOS,
              UN_VALORES => MI_VALORES
          );
      EXCEPTION
          WHEN PCK_EXCEPCIONES.EXC_INSERTAR THEN
              RAISE PCK_EXCEPCIONES.EXC_ALMACEN;
      END;
       EXCEPTION WHEN PCK_EXCEPCIONES.EXC_ALMACEN THEN
                                    PCK_ERR_MSG.RAISE_WITH_MSG(
                                    UN_EXC_COD   => SQLCODE,
                                    UN_ERROR_COD => PCK_ERRORES.ERR_ALMACEN_D_MOVIMIENTO_MOV
