                          AND SALDO_AUX_CONTABLE.ANO            = FUENTE_RECURSOS.ANO
                          AND SALDO_AUX_CONTABLE.FUENTE_RECURSO = FUENTE_RECURSOS.CODIGO
                      INNER JOIN REFERENCIA
                          ON  SALDO_AUX_CONTABLE.COMPANIA   = REFERENCIA.COMPANIA
                          AND SALDO_AUX_CONTABLE.ANO        = REFERENCIA.ANO
                          AND SALDO_AUX_CONTABLE.REFERENCIA = REFERENCIA.CODIGO
                      WHERE PLAN_CONTABLE.COMPANIA   = UN_COMPANIA
                        AND PLAN_CONTABLE.ANO        = V_ANO
                        AND PLAN_CONTABLE.CODIGO     BETWEEN UN_CODIGO_INI AND UN_CODIGO_FIN
                        AND PLAN_CONTABLE.MOVIMIENTO IN (0)
                      GROUP BY
                          PLAN_CONTABLE.COMPANIA,
                          PLAN_CONTABLE.ANO,
                          PLAN_CONTABLE.CODIGO,
                          PLAN_CONTABLE.NATURALEZA,
                          PLAN_CONTABLE.CONCEPTOS_PATRIMONIO,
                          CASE WHEN 1=0 THEN SALDO_AUX_CONTABLE.TERCERO        ELSE NULL END,
                          CASE WHEN 1=0 THEN SALDO_AUX_CONTABLE.AUXILIAR       ELSE NULL END,
                          CASE WHEN 1=0 THEN SALDO_AUX_CONTABLE.CENTRO_COSTO   ELSE NULL END,
                          CASE WHEN 1=0 THEN SALDO_AUX_CONTABLE.REFERENCIA     ELSE NULL END,
                          CASE WHEN 1=0 THEN SALDO_AUX_CONTABLE.FUENTE_RECURSO ELSE NULL END,
                          CASE WHEN 1=0 THEN TERCERO.NOMBRE                    ELSE NULL END,
                          CASE WHEN 1=0 THEN AUXILIAR.NOMBRE                   ELSE NULL END,
                          CASE WHEN 1=0 THEN CENTRO_COSTO.NOMBRE               ELSE NULL END,
                          CASE WHEN 1=0 THEN REFERENCIA.NOMBRE                 ELSE NULL END,
                          CASE WHEN 1=0 THEN FUENTE_RECURSOS.NOMBRE            ELSE NULL END,
                          PLAN_CONTABLE.NOMBRE ||
                              CASE WHEN 1=0 AND PLAN_CONTABLE.MAN_AUX_TER<>0 THEN '/'||TERCERO.NOMBRE         ELSE NULL END ||
                              CASE WHEN 1=0 AND PLAN_CONTABLE.MAN_AUX_GEN<>0 THEN '/'||AUXILIAR.NOMBRE        ELSE NULL END ||
                              CASE WHEN 1=0 AND PLAN_CONTABLE.MAN_CEN_CTO<>0 THEN '/'||CENTRO_COSTO.NOMBRE    ELSE NULL END ||
                              CASE WHEN 1=0 AND PLAN_CONTABLE.MAN_AUX_REF<>0 THEN '/'||REFERENCIA.NOMBRE      ELSE NULL END ||
                              CASE WHEN 1=0 AND PLAN_CONTABLE.MAN_AUX_FUE<>0 THEN '/'||FUENTE_RECURSOS.NOMBRE ELSE NULL END,
                          PLAN_CONTABLE.CODIGO ||
                              CASE WHEN 1=0 AND PLAN_CONTABLE.MAN_AUX_TER<>0 THEN '/'||SALDO_AUX_CONTABLE.TERCERO        ELSE NULL END ||
                              CASE WHEN 1=0 AND PLAN_CONTABLE.MAN_AUX_GEN<>0 THEN '/'||SALDO_AUX_CONTABLE.AUXILIAR       ELSE NULL END ||
                              CASE WHEN 1=0 AND PLAN_CONTABLE.MAN_CEN_CTO<>0 THEN '/'||SALDO_AUX_CONTABLE.CENTRO_COSTO   ELSE NULL END ||
                              CASE WHEN 1=0 AND PLAN_CONTABLE.MAN_AUX_REF<>0 THEN '/'||SALDO_AUX_CONTABLE.REFERENCIA     ELSE NULL END ||
                              CASE WHEN 1=0 AND PLAN_CONTABLE.MAN_AUX_FUE<>0 THEN '/'||SALDO_AUX_CONTABLE.FUENTE_RECURSO ELSE NULL END
                  ) SALDOAUX
                  WHERE LENGTH(SALDOAUX.CODIGO) <= 30
              ),
             SALDO13_AGR AS (
    SELECT
        PC.COMPANIA,
        PC.ANO,
        SUBSTR(PC.CODIGO, 1, 4)         AS CODIGO,
        PC.CONCEPTOS_PATRIMONIO,
        PC.COLUMNA_REPORTE_PATR,
        SUM(PC.CREDITO13 - PC.DEBITO13) AS VALOR_S13
    FROM PLAN_CONTABLE PC
    WHERE PC.COMPANIA = UN_COMPANIA
      AND PC.ANO      = V_ANO
      AND PC.CODIGO   BETWEEN UN_CODIGO_INI AND UN_CODIGO_FIN
    GROUP BY
        PC.COMPANIA, PC.ANO,
        SUBSTR(PC.CODIGO, 1, 4),
        PC.CONCEPTOS_PATRIMONIO,
        PC.COLUMNA_REPORTE_PATR
),
MOV_AGR_COL AS (
    SELECT
        MA.COMPANIA,
        MA.ANO,
        MA.CONCEPTOS_PATRIMONIO,
        PC2.COLUMNA_REPORTE_PATR,
        SUM(MA.VALOR) AS VALOR
    FROM MOV_AGR MA
    INNER JOIN PLAN_CONTABLE PC2
        ON  PC2.COMPANIA = MA.COMPANIA
        AND PC2.ANO      = MA.ANO
        AND PC2.CODIGO   = MA.CODIGO
    WHERE PC2.COLUMNA_REPORTE_PATR IS NOT NULL
    GROUP BY
        MA.COMPANIA,
        MA.ANO,
        MA.CONCEPTOS_PATRIMONIO,
        PC2.COLUMNA_REPORTE_PATR
)
SELECT
    CB.CONCEPTO_COD,
    CB.SECCION,
    CB.GRUPO,
    CB.ANO,
    NVL(MAX(CASE WHEN M.COLUMNA_REPORTE_PATR = 'CAPITAL'          THEN M.VALOR END), 0)  AS CAPITAL,
    NVL(MAX(CASE WHEN M.COLUMNA_REPORTE_PATR = 'RESERVAS'         THEN M.VALOR END), 0)  AS RESERVAS,
   NVL(MAX(CASE WHEN M.COLUMNA_REPORTE_PATR = 'RESULTADOS' THEN M.VALOR END), 0)
    +  NVL( MAX(CASE WHEN S.COLUMNA_REPORTE_PATR = 'RESULTADOS' THEN S.VALOR_S13 END), 0) AS RESULTADO_EJERCICIO,
    NVL(MAX(CASE WHEN M.COLUMNA_REPORTE_PATR = 'RESULTADOS_ANT'   THEN M.VALOR END), 0)  AS RESULTADO_EJER_ANT,
    NVL(MAX(CASE WHEN M.COLUMNA_REPORTE_PATR = 'OTRAS'            THEN M.VALOR END), 0)  AS OTRAS_PARTIDAS
FROM CONCEPTOS_BASE CB
LEFT JOIN MOV_AGR_COL M
    ON  M.COMPANIA             = CB.COMPANIA
    AND M.ANO                  = CB.ANO
    AND M.CONCEPTOS_PATRIMONIO = CB.CONCEPTO_COD
LEFT JOIN SALDO13_AGR S
    ON  S.COMPANIA             = CB.COMPANIA
    AND S.ANO                  = CB.ANO
    AND S.CONCEPTOS_PATRIMONIO = CB.CONCEPTO_COD
GROUP BY
    CB.CONCEPTO_COD, CB.SECCION, CB.GRUPO, CB.ANO
ORDER BY
    CB.CONCEPTO_COD, CB.GRUPO, CB.SECCION
        )
        LOOP
              MI_ORDEN  := MI_ORDEN + 1;
              MI_VALORES := '''MOV'', '                                   ||
                        V_ANO || ', ' || MI_ORDEN                     ||
                        ', ''' || RS_MOV.SECCION || ''', '''            ||
                        RS_MOV.CAPITAL              || ''', '''           ||
                        RS_MOV.RESERVAS             || ''', '''          ||
                        RS_MOV.RESULTADO_EJERCICIO  || ''', '''           ||
                        RS_MOV.RESULTADO_EJER_ANT   || ''', '''           ||
                        RS_MOV.OTRAS_PARTIDAS || ''' ';
          BEGIN
              PCK_DATOS.GL_RTA := PCK_DATOS.FC_ACME(
                  UN_TABLA   => 'TEMP_ESTADOS_PATRIMONIO',
                  UN_ACCION  => 'I',
                  UN_CAMPOS  => MI_CAMPOS,
                  UN_VALORES => MI_VALORES
              );
          EXCEPTION
              WHEN PCK_EXCEPCIONES.EXC_INSERTAR THEN
                  RAISE PCK_EXCEPCIONES.EXC_ALMACEN;
          END;
          V_CAPITAL    := V_CAPITAL    + RS_MOV.CAPITAL;
          V_RESERVAS   := V_RESERVAS   + RS_MOV.RESERVAS;
          V_RESULTADO  := V_RESULTADO  + RS_MOV.RESULTADO_EJERCICIO;
          V_RESULT_ANT := V_RESULT_ANT + RS_MOV.RESULTADO_EJER_ANT;
          V_OTRAS      := V_OTRAS      + RS_MOV.OTRAS_PARTIDAS;
        END LOOP;
      IF V_ANO = TO_NUMBER(UN_ANIO_FIN) THEN
      MI_ORDEN  := MI_ORDEN + 1;
      MI_VALORES := '''SALDO_FIN'', '                                 ||
                    V_ANO || ', ' || MI_ORDEN                         ||
                    ', ''Saldo al 31 de diciembre de '                ||
                    TO_CHAR(V_ANO) || ''', '''                          ||
                    V_CAPITAL    || ''', ''' || V_RESERVAS   || ''', '''      ||
                    V_RESULTADO  || ''', ''' || V_RESULT_ANT || ''', '''      ||
                    V_OTRAS || ''' ';
      BEGIN
          PCK_DATOS.GL_RTA := PCK_DATOS.FC_ACME(
              UN_TABLA   => 'TEMP_ESTADOS_PATRIMONIO',
              UN_ACCION  => 'I',
              UN_CAMPOS  => MI_CAMPOS,
              UN_VALORES => MI_VALORES
          );
      EXCEPTION
