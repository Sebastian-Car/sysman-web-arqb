          AND CUENTA   = UN_CUENTA
          AND TIPO_COMPROBANTE  = UN_TIPO
          AND IND_CAUSACION NOT IN(0);
        IF MI_CONTROLAFECTA = 0 THEN
            MI_EXISTE:= 0;
            SELECT COUNT(COMPANIA)
              INTO MI_EXISTE
              FROM DETALLE_COMPROBANTE_CNT
            WHERE COMPANIA      = UN_COMPANIA
              AND ANO           = UN_ANO_AFECT
              AND TIPO_CPTE     = UN_TIPO_CPTE_AFECT
              AND COMPROBANTE   = UN_CMPTE_AFECTADO
              AND CONSECUTIVO   = UN_CONSECUTIVOAFECTADO
              AND NRO_DOCUMENTO = UN_NRO_DOCUMENTO;
            IF MI_EXISTE=0 THEN
                BEGIN
                    RAISE PCK_EXCEPCIONES.EXC_CONTABILIDAD;
                EXCEPTION WHEN PCK_EXCEPCIONES.EXC_CONTABILIDAD THEN 
                               PCK_ERR_MSG.RAISE_WITH_MSG(
                               UN_EXC_COD =>SQLCODE,
                               UN_ERROR_COD=>PCK_ERRORES.ERR_CONTA_NRO_DOCUMENTOAFCT);  
                END;
            END IF;
        END IF;
    END IF;
END PR_CONTROLACUENTA;

