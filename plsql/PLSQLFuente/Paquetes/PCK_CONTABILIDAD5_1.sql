  --1. verificarSaldoRegistro
  FUNCTION FC_VERIFICARSALDOREGISTRO 
  /*
    NAME              : FC_VERIFICARSALDOREGISTRO
    AUTHORS           : SYSMAN
    AUTHOR MIGRACION  : JUAN CARLOS RODR�?GUEZ AMÉZQUITA
    DATE MIGRADOR     : 07/12/2016
    TIME              : 04:50 PM
    MODIFIER          : 
    DATE MODIFIED     : 
    TIME              : 
    DESCRIPTION       : Verifica el saldo del registro con respecto a la orden de 
                        pago de contabilidad.
    MODIFICATIONS     : 
    PARAMETROS DE ENTRADA: 
      UN_COMPANIA:        Código de la compañía
      UN_ANO:             Año en el cual se realiza el comprobante
      UN_TIPO_CPTE:       Tipo de comprobante presupuestal 
      UN_COMPROBANTE:     Número del comprobante presupuestal
      UN_VALORDOCUMENTO:  Valor total del comprobante incluido las retenciones

    @NAME: verificarSaldoRegistro
    @METHOD: get
  */ 
  (
    UN_COMPANIA               IN PCK_SUBTIPOS.TI_COMPANIA 
  , UN_ANO                    IN PCK_SUBTIPOS.TI_ANIO 
  , UN_TIPO_CPTE              IN PCK_SUBTIPOS.TI_TIPOCOMPROBANTEPPTAL 
  , UN_COMPROBANTE            IN PCK_SUBTIPOS.TI_NUMEROCOMPROBANTEPPTAL 
  , UN_VALORDOCUMENTO         IN PCK_SUBTIPOS.TI_DOBLE 
  ) RETURN NUMBER AS
    MI_REEMPLAZOS             PCK_SUBTIPOS.TI_CLAVEVALOR;
    MI_SALDO                  NUMBER := 0;
    CURSOR MI_CURSOR
    IS
      SELECT NATURALEZA, 
             SUM(VALOR_DEBITO) VALOR_DEBITO, 
             SUM(VALOR_CREDITO) VALOR_CREDITO, 
             SUM(DEBITO_AFECTADO) DEBITO_AFECTADO, 
             SUM(CREDITO_AFECTADO) CREDITO_AFECTADO, 
             SUM(MODIFICACION_DEBITO) MODIFICACION_DEBITO, 
             SUM(MODIFICACION_CREDITO) MODIFICACION_CREDITO
        FROM DETALLE_COMPROBANTE_PPTAL
       WHERE COMPANIA = UN_COMPANIA
         AND ANO = UN_ANO
         AND TIPO_CPTE = UN_TIPO_CPTE
         AND COMPROBANTE = UN_COMPROBANTE 
       GROUP BY NATURALEZA;
BEGIN
    FOR RS  IN  MI_CURSOR
    LOOP
      CASE
          WHEN RS.NATURALEZA = 'D' 
          THEN 
            MI_SALDO := MI_SALDO + (RS.VALOR_DEBITO + RS.VALOR_CREDITO) - 
            (RS.DEBITO_AFECTADO + RS.CREDITO_AFECTADO) + 
            (RS.MODIFICACION_DEBITO + RS.MODIFICACION_CREDITO);
          ELSE 
            MI_SALDO := MI_SALDO + (RS.VALOR_CREDITO + RS.VALOR_DEBITO) - 
            (RS.CREDITO_AFECTADO + RS.DEBITO_AFECTADO) + 
            (RS.MODIFICACION_CREDITO + RS.MODIFICACION_DEBITO);
        END CASE;
    END LOOP;
    BEGIN--
        IF MI_SALDO < UN_VALORDOCUMENTO THEN
          -- verificarSaldoRegistro = False

          RAISE PCK_EXCEPCIONES.EXC_CONTABILIDAD;
          --RETURN 0;
        END IF;
    EXCEPTION  WHEN PCK_EXCEPCIONES.EXC_CONTABILIDAD THEN 
        MI_REEMPLAZOS(1).CLAVE := 'SALDO';
        MI_REEMPLAZOS(1).VALOR := MI_SALDO;
        MI_REEMPLAZOS(2).CLAVE := 'VALORDOCUMENTO';
        MI_REEMPLAZOS(2).VALOR := UN_VALORDOCUMENTO;
        MI_REEMPLAZOS(3).CLAVE := 'DIFERENCIA';
        MI_REEMPLAZOS(3).VALOR := MI_SALDO - UN_VALORDOCUMENTO;
        PCK_ERR_MSG.RAISE_WITH_MSG(
            UN_EXC_COD => SQLCODE,
            UN_TABLAERROR=>'DETALLE_COMPROBANTE_PPTAL',
            UN_ERROR_COD => PCK_ERRORES.ERR_CONTABILIDAD_VERIFSALDOREG,
            UN_REEMPLAZOS => MI_REEMPLAZOS
          );
     END;
 RETURN -1;
END FC_VERIFICARSALDOREGISTRO;

