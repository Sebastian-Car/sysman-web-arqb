               CASE WHEN AF.COMPANIA IS NULL OR TC.CLASE_CONTABLE = 'U' THEN DC.CREDITO_AFECTADO
                  ELSE 0 END
    )
    GROUP BY COMPANIA, 
             ANO,
             TIPO_CPTE,
             COMPROBANTE,
             CONSECUTIVO
    HAVING SUM(DEBITO_AFECTADO) <> SUM(DEBITO_AFECTADO_CAL)
