  V_RESERVAS            NUMBER(20,2) := 0;
  V_RESULTADO           NUMBER(20,2) := 0;
  V_RESULT_ANT          NUMBER(20,2) := 0;
  V_OTRAS               NUMBER(20,2) := 0;
  V_TIENE_DATOS         BOOLEAN := FALSE;
BEGIN
     BEGIN
       MI_STRSQL:=' DELETE FROM TEMP_ESTADOS_PATRIMONIO ' ;
        EXECUTE IMMEDIATE MI_STRSQL;
       END;
  FOR RS_SALDO_INI  IN (SELECT *
                FROM (
                    SELECT
                        PC.COLUMNA_REPORTE_PATR,
                        PC.COMPANIA,
                        PC.ANO,
                        SUM(PC.SALDO13) AS SANTERIOR 
                    FROM V_PLAN_CONTABLE PC
                    WHERE PC.COMPANIA   = UN_COMPANIA 
                      AND PC.ANO        = UN_ANIO_INI - 1
                      AND PC.CODIGO     LIKE '3%'
                      AND COLUMNA_REPORTE_PATR IS NOT NULL
                    GROUP BY
                        PC.COLUMNA_REPORTE_PATR,
                        PC.COMPANIA,
                        PC.ANO
                )
                PIVOT (
                    SUM(SANTERIOR)
                    FOR COLUMNA_REPORTE_PATR IN (
                        'CAPITAL' AS CAPITAL,
                        'RESERVAS' AS RESERVAS,
                        'RESULTADOS' AS RESULTADO_EJERCICIO,
                        'RESULTADOS_ANT' AS RESULTADO_EJER_ANT,
                        'OTRAS' AS OTRAS_PARTIDAS
                    )))
    LOOP
      V_CAPITAL    := NVL(RS_SALDO_INI.CAPITAL,             0);
      V_RESERVAS   := NVL(RS_SALDO_INI.RESERVAS,            0);
      V_RESULTADO  := NVL(RS_SALDO_INI.RESULTADO_EJERCICIO, 0);
      V_RESULT_ANT := NVL(RS_SALDO_INI.RESULTADO_EJER_ANT,  0);
      V_OTRAS      := NVL(RS_SALDO_INI.OTRAS_PARTIDAS,       0);
