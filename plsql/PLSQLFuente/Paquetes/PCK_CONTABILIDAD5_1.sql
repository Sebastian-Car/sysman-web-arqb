       END;
        FOR RS_MOV  IN (WITH CONCEPTOS_BASE AS (
                  SELECT DISTINCT
                      CC.COMPANIA,
                      CC.CODIGO       AS CONCEPTO_COD,
                      CC.NOMBRE       AS SECCION,
                      SC.CODIGO       AS GRUPO,
                      CC.ANO
                  FROM CONFIGURAR_CONCEPTOS CC
                  INNER JOIN SECCIONES_CONCEPTOS SC
                      ON  CC.COMPANIA = SC.COMPANIA
                      AND CC.SECCION  = SC.CODIGO
                  WHERE CC.COMPANIA = UN_COMPANIA
                    AND CC.INFORME  = '002915EstadoCambiosPatrimonio'
                    AND CC.ANO      = V_ANO
                    ORDER BY CONCEPTO_COD
              ),
              MOV_AGR AS (
                  SELECT SALDOAUX.COMPANIA,
                         SALDOAUX.ANO,
                         SALDOAUX.CODIGO,
                         SALDOAUX.CREDITO - SALDOAUX.DEBITO AS VALOR,
                         SALDOAUX.CONCEPTOS_PATRIMONIO
                  FROM (
                      SELECT
                          PLAN_CONTABLE.COMPANIA,
                          PLAN_CONTABLE.ANO,
                          PLAN_CONTABLE.CODIGO,
                          PLAN_CONTABLE.NATURALEZA,
                          PLAN_CONTABLE.CONCEPTOS_PATRIMONIO,
                          (CASE WHEN 0  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.DEBITO0  ELSE 0 END
                            +CASE WHEN 1  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.DEBITO1  ELSE 0 END
                            +CASE WHEN 2  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.DEBITO2  ELSE 0 END
                            +CASE WHEN 3  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.DEBITO3  ELSE 0 END
                            +CASE WHEN 4  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.DEBITO4  ELSE 0 END
                            +CASE WHEN 5  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.DEBITO5  ELSE 0 END
                            +CASE WHEN 6  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.DEBITO6  ELSE 0 END
                            +CASE WHEN 7  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.DEBITO7  ELSE 0 END
                            +CASE WHEN 8  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.DEBITO8  ELSE 0 END
                            +CASE WHEN 9  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.DEBITO9  ELSE 0 END
                            +CASE WHEN 10 BETWEEN 1 AND 12 THEN PLAN_CONTABLE.DEBITO10 ELSE 0 END
                            +CASE WHEN 11 BETWEEN 1 AND 12 THEN PLAN_CONTABLE.DEBITO11 ELSE 0 END
                            +CASE WHEN 12 BETWEEN 1 AND 12 THEN PLAN_CONTABLE.DEBITO12 ELSE 0 END
                            +CASE WHEN 13 BETWEEN 1 AND 12 THEN PLAN_CONTABLE.DEBITO13 ELSE 0 END
                          ) DEBITO,
                          (CASE WHEN 0  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.CREDITO0  ELSE 0 END
                            +CASE WHEN 1  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.CREDITO1  ELSE 0 END
                            +CASE WHEN 2  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.CREDITO2  ELSE 0 END
                            +CASE WHEN 3  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.CREDITO3  ELSE 0 END
                            +CASE WHEN 4  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.CREDITO4  ELSE 0 END
                            +CASE WHEN 5  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.CREDITO5  ELSE 0 END
                            +CASE WHEN 6  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.CREDITO6  ELSE 0 END
                            +CASE WHEN 7  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.CREDITO7  ELSE 0 END
                            +CASE WHEN 8  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.CREDITO8  ELSE 0 END
                            +CASE WHEN 9  BETWEEN 1 AND 12 THEN PLAN_CONTABLE.CREDITO9  ELSE 0 END
                            +CASE WHEN 10 BETWEEN 1 AND 12 THEN PLAN_CONTABLE.CREDITO10 ELSE 0 END
                            +CASE WHEN 11 BETWEEN 1 AND 12 THEN PLAN_CONTABLE.CREDITO11 ELSE 0 END
                            +CASE WHEN 12 BETWEEN 1 AND 12 THEN PLAN_CONTABLE.CREDITO12 ELSE 0 END
                            +CASE WHEN 13 BETWEEN 1 AND 12 THEN PLAN_CONTABLE.CREDITO13 ELSE 0 END
                          ) CREDITO
                      FROM PLAN_CONTABLE
                      WHERE PLAN_CONTABLE.COMPANIA = UN_COMPANIA
                        AND PLAN_CONTABLE.ANO      = V_ANO
                        AND PLAN_CONTABLE.CODIGO   BETWEEN UN_CODIGO_INI AND UN_CODIGO_FIN
                      UNION
                      SELECT
                          PLAN_CONTABLE.COMPANIA,
                          PLAN_CONTABLE.ANO,
                          PLAN_CONTABLE.CODIGO,
                          PLAN_CONTABLE.NATURALEZA,
                          PLAN_CONTABLE.CONCEPTOS_PATRIMONIO,
                          SUM(CASE WHEN 0  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.DEBITO0  ELSE 0 END
                             +CASE WHEN 1  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.DEBITO1  ELSE 0 END
                             +CASE WHEN 2  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.DEBITO2  ELSE 0 END
                             +CASE WHEN 3  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.DEBITO3  ELSE 0 END
                             +CASE WHEN 4  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.DEBITO4  ELSE 0 END
                             +CASE WHEN 5  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.DEBITO5  ELSE 0 END
                             +CASE WHEN 6  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.DEBITO6  ELSE 0 END
                             +CASE WHEN 7  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.DEBITO7  ELSE 0 END
                             +CASE WHEN 8  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.DEBITO8  ELSE 0 END
                             +CASE WHEN 9  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.DEBITO9  ELSE 0 END
                             +CASE WHEN 10 BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.DEBITO10 ELSE 0 END
                             +CASE WHEN 11 BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.DEBITO11 ELSE 0 END
                             +CASE WHEN 12 BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.DEBITO12 ELSE 0 END
                             +CASE WHEN 13 BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.DEBITO13 ELSE 0 END
                          ) DEBITO,
                          SUM(CASE WHEN 0  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.CREDITO0  ELSE 0 END
                             +CASE WHEN 1  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.CREDITO1  ELSE 0 END
                             +CASE WHEN 2  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.CREDITO2  ELSE 0 END
                             +CASE WHEN 3  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.CREDITO3  ELSE 0 END
                             +CASE WHEN 4  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.CREDITO4  ELSE 0 END
                             +CASE WHEN 5  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.CREDITO5  ELSE 0 END
                             +CASE WHEN 6  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.CREDITO6  ELSE 0 END
                             +CASE WHEN 7  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.CREDITO7  ELSE 0 END
                             +CASE WHEN 8  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.CREDITO8  ELSE 0 END
                             +CASE WHEN 9  BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.CREDITO9  ELSE 0 END
                             +CASE WHEN 10 BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.CREDITO10 ELSE 0 END
                             +CASE WHEN 11 BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.CREDITO11 ELSE 0 END
                             +CASE WHEN 12 BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.CREDITO12 ELSE 0 END
                             +CASE WHEN 13 BETWEEN 1 AND 12 THEN SALDO_AUX_CONTABLE.CREDITO13 ELSE 0 END
                          ) CREDITO
                      FROM PLAN_CONTABLE
                      INNER JOIN SALDO_AUX_CONTABLE
                          ON  PLAN_CONTABLE.COMPANIA = SALDO_AUX_CONTABLE.COMPANIA
                          AND PLAN_CONTABLE.ANO      = SALDO_AUX_CONTABLE.ANO
                          AND PLAN_CONTABLE.CODIGO   = SALDO_AUX_CONTABLE.CODIGO
                      INNER JOIN TERCERO
                          ON  SALDO_AUX_CONTABLE.COMPANIA = TERCERO.COMPANIA
                          AND SALDO_AUX_CONTABLE.TERCERO  = TERCERO.NIT
                          AND SALDO_AUX_CONTABLE.SUCURSAL = TERCERO.SUCURSAL
                      INNER JOIN CENTRO_COSTO
                          ON  SALDO_AUX_CONTABLE.COMPANIA     = CENTRO_COSTO.COMPANIA
                          AND SALDO_AUX_CONTABLE.ANO          = CENTRO_COSTO.ANO
                          AND SALDO_AUX_CONTABLE.CENTRO_COSTO = CENTRO_COSTO.CODIGO
                      INNER JOIN AUXILIAR
                          ON  SALDO_AUX_CONTABLE.COMPANIA = AUXILIAR.COMPANIA
                          AND SALDO_AUX_CONTABLE.ANO      = AUXILIAR.ANO
                          AND SALDO_AUX_CONTABLE.AUXILIAR = AUXILIAR.CODIGO
                      INNER JOIN FUENTE_RECURSOS
