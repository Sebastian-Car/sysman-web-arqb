    UN_PERIODO1     IN PCK_SUBTIPOS.TI_PERIODO,
    UN_BANCO        IN PERSONAL.BANCO%TYPE,
    UN_FECHA        IN DATE
  )
  RETURN CLOB
  AS 
    MI_NUMCUENTAS   PCK_SUBTIPOS.TI_ENTERO_LARGO := 0;
    MI_TOTALPAGO    PCK_SUBTIPOS.TI_ENTERO_LARGO := 0;
    MI_SALIDA       CLOB;
    MI_AUX          PCK_SUBTIPOS.TI_ENTERO_LARGO := 0;
    MI_CUENTAB      VARCHAR2(3000 CHAR) := '';
  BEGIN
    <<HISTORICOPERSONAL>>
    FOR MI_P IN(SELECT 
                   PERSONAL.BANCO
                  ,BANCOS_NOMINA.CUENTA NUMCUENTA
                  ,HISTORICOS.ID_DE_PROCESO
                  ,HISTORICOS.ANO
                  ,HISTORICOS.MES
                  ,HISTORICOS.PERIODO
                  ,HISTORICOS.ID_DE_EMPLEADO
                  ,HISTORICOS.ID_DE_CONCEPTO
                  ,HISTORICOS.VALOR
                  ,PERSONAL.CUENTA
                  ,PERSONAL.TIPOCUENTA
                  ,PERSONAL.NUMERO_DCTO
                  ,PERSONAL.NOMBRECOMPLETO NOMCOMPLETO
                FROM PERSONAL 
                  INNER JOIN BANCOS_NOMINA
                     ON PERSONAL.COMPANIA       = BANCOS_NOMINA.COMPANIA
                    AND PERSONAL.BANCO          = BANCOS_NOMINA.BANCO 
                  LEFT JOIN HISTORICOS 
                    ON PERSONAL.COMPANIA       = HISTORICOS.COMPANIA
                   AND PERSONAL.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
                 WHERE PERSONAL.COMPANIA       = UN_COMPANIA
                   AND PERSONAL.BANCO            = UN_BANCO
                   AND HISTORICOS.ID_DE_PROCESO  = UN_PROCESO
                   AND HISTORICOS.ANO            = UN_ANO1
                   AND HISTORICOS.MES            = UN_MES1
                   AND HISTORICOS.PERIODO        = UN_PERIODO1
                   AND HISTORICOS.ID_DE_CONCEPTO = 144
                 ORDER BY PERSONAL.CUENTA)
    LOOP
      IF MI_AUX IN(0) THEN
        MI_CUENTAB := MI_P.NUMCUENTA;
        MI_CUENTAB := REPLACE(MI_CUENTAB, '-','');
        MI_CUENTAB := REPLACE(MI_CUENTAB, '.', '');
      END IF;

      MI_AUX := MI_AUX + 1;
      MI_NUMCUENTAS := MI_NUMCUENTAS + 1;

      MI_TOTALPAGO := MI_TOTALPAGO + 
                      PCK_SYSMAN_UTL.FC_ROUND(UN_VALOR     => MI_P.VALOR
                                             ,UN_PRECISION => 0);      
    END LOOP HISTORICOPERSONAL;

    MI_SALIDA  := MI_CUENTAB;
    MI_SALIDA  := REPLACE(MI_SALIDA, '-','');
    MI_SALIDA  := REPLACE(MI_SALIDA, '.','');
    MI_SALIDA := PCK_SYSMAN_UTL.FC_STRZERO(UN_NUMERO   => MI_CUENTAB
                                          ,UN_LONGITUD => 4);    
    MI_SALIDA := MI_SALIDA||MI_CUENTAB||chr(10);

    MI_SALIDA := MI_SALIDA
               ||'005'
               ||'000000000'
               ||TO_CHAR(UN_FECHA, 'YYMMdd')
               ||'D'
               ||'0001'
               ||PCK_SYSMAN_UTL.FC_STRZERO(UN_NUMERO   => MI_TOTALPAGO
                                          ,UN_LONGITUD => 13)
               ||'00'
               ||PCK_SYSMAN_UTL.FC_STRZERO(UN_NUMERO   => 0
                                          ,UN_LONGITUD => 30)
               ||PCK_SYSMAN_UTL.FC_STRZERO(UN_NUMERO   => 0
                                          ,UN_LONGITUD => 9)
