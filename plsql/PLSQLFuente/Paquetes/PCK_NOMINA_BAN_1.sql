FUNCTION FC_PLANOPAGOTERCSUDAMERIS 

  /*
      NAME              : FC_PLANOPAGOTERCSUDAMERIS
      AUTHORS           : SYSMAN  SAS
      AUTHOR MIGRACION  : ELKIN GEOVANNY AMAYA SILVA
      DATE MIGRADOR     : 24/05/2019
      TIME              : 11 :30 AM
      MODIFIER          : APINEDA
      DATE MODIFIED     : 01/11/2019
      TIME              : 04:31
      DESCRIPTION       : Funcion que genera el archivo plano para pago a terceros del banco SUDAMERIS
      @Name: generarPlanoPagoTerSudameris
    */

  (   
      UN_COMPANIA         IN PCK_SUBTIPOS.TI_COMPANIA,
      UN_ANO              IN PCK_SUBTIPOS.TI_ANIO,
      UN_MES              IN PCK_SUBTIPOS.TI_MES,
      UN_PERIODO          IN PCK_SUBTIPOS.TI_PERIODO,
      UN_PROCESO          IN PCK_SUBTIPOS.TI_ID_DE_PROCESO,
      UN_DESCRIPCIONDETA  IN VARCHAR2,
      UN_DESCRIPCIONPAGO  IN VARCHAR2,
      UN_DESCRIPCIONAMPL  IN VARCHAR2,
      UN_TIPOPLANO        IN PLS_INTEGER,
      UN_CENTROCOSTO      IN VARCHAR2
  ) RETURN CLOB

  AS  
      MI_PLANO              CLOB:= '';
      MI_AC                 NUMBER:=0;
      MI_CUENTASUDAMERIS    BANCOS_NOMINA.CUENTA%TYPE := ' ';
      MI_TIPOSUDAMERIS      BANCOS_NOMINA.TIPO_CUENTA%TYPE := ' ';
      MI_NACHAMSUDAMERIS    BANCOS_NOMINA.CODIGO_NACHAM%TYPE := ' ';
      MI_RAZONSOCIAL        TERCERO.NOMBRE%TYPE := '';
      --(APINEDA:01/11/2019)-Bucaramanga Punto 3 de TAR1000095436
      RS SYS_REFCURSOR;
      MI_STRSQL             PCK_SUBTIPOS.TI_CONSULTA;
      MI_PARAMETRO          VARCHAR2(30 CHAR);      
      MI_CENTROCOSTO        VARCHAR2(20 CHAR);
      MI_CODIGO_NACHAM      VARCHAR2(12 CHAR);
      MI_TIPO_CUENTA        VARCHAR2(1 CHAR);
      MI_C_CUENTA           VARCHAR2(15 CHAR);
      MI_NIT                VARCHAR2(18 CHAR);
      MI_NOMBRE             VARCHAR2(323 CHAR);
      MI_DIRECCIONEMAIL     VARCHAR2(80 CHAR);
      MI_VALOR              NUMBER:=0;
  BEGIN

        SELECT BANCOS_NOMINA.CODIGO_NACHAM,
            BANCOS_NOMINA.CUENTA,
            CASE BANCOS_NOMINA.TIPO_CUENTA
                WHEN 'A'   THEN '2'
                WHEN 'C'   THEN '1'
                ELSE ''
            END TIPO_CUENTA
        INTO MI_NACHAMSUDAMERIS ,MI_CUENTASUDAMERIS, MI_TIPOSUDAMERIS
        FROM
            BANCOS_NOMINA
        WHERE
            BANCOS_NOMINA.COMPANIA   = UN_COMPANIA
            AND BANCOS_NOMINA.NOMBRE LIKE '%SUDAMERIS%';


      BEGIN
          BEGIN
              SELECT NOMBRE
                INTO MI_RAZONSOCIAL
                FROM TERCERO
               WHERE COMPANIA = UN_COMPANIA
                 AND NIT = '890201222'
                 AND SUCURSAL = '001';
          EXCEPTION WHEN NO_DATA_FOUND THEN
              RAISE PCK_EXCEPCIONES.EXC_NOMINA;
          END;
      EXCEPTION WHEN PCK_EXCEPCIONES.EXC_NOMINA THEN
          PCK_ERR_MSG.RAISE_WITH_MSG
                  ( UN_EXC_COD => SQLCODE
                  , UN_TABLAERROR => 'TERCERO'
                  , UN_ERROR_COD  => PCK_ERRORES.ERR_ENTIDADNOEXISTE);
      END;

      MI_STRSQL := 'SELECT BANCOS_NOMINA.CODIGO_NACHAM,
                         CASE CONCEPTOS.C_TIPO_CUENTA
                             WHEN ''A''   THEN ''2''
                             WHEN ''C''   THEN ''1''
                             ELSE ''''
                         END TIPO_CUENTA,
                         CONCEPTOS.C_CUENTA,
                         TERCERO.NIT,
                         TERCERO.NOMBRE,
                         TERCERO.DIRECCIONEMAIL,
                         SUM(HISTORICOS.VALOR) VALOR
                     FROM CONCEPTOS
                         INNER JOIN HISTORICOS 
                                ON CONCEPTOS.COMPANIA         = HISTORICOS.COMPANIA
                               AND CONCEPTOS.ID_DE_CONCEPTO   = HISTORICOS.ID_DE_CONCEPTO
                         INNER JOIN TERCERO 
                                ON CONCEPTOS.COMPANIA   = TERCERO.COMPANIA
                               AND CONCEPTOS.TERCERO    = TERCERO.NIT
                               AND CONCEPTOS.SUCURSAL   = TERCERO.SUCURSAL
                         LEFT JOIN BANCO 
                                ON CONCEPTOS.COMPANIA   = BANCO.COMPANIA
                               AND CONCEPTOS.C_BANCO    = BANCO.BANCO
                         LEFT JOIN BANCOS_NOMINA 
                                ON BANCO.COMPANIA   = BANCOS_NOMINA.COMPANIA
                               AND BANCO.BANCO      = BANCOS_NOMINA.BANCO
                         INNER JOIN PERSONAL 
                                ON PERSONAL.COMPANIA = HISTORICOS.COMPANIA
                               AND PERSONAL.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
                         WHERE HISTORICOS.COMPANIA          = ''' || UN_COMPANIA || ''' 
                             AND HISTORICOS.ANO             = ' || UN_ANO || '
                             AND HISTORICOS.MES             = ' || UN_MES || '
                             AND HISTORICOS.PERIODO         = ' || UN_PERIODO || '
                             AND HISTORICOS.ID_DE_PROCESO   = ' || UN_PROCESO || '
                             AND CONCEPTOS.MANEJA_PAGO_TERCEROS NOT IN (0) ';

      --Plano para un solo centro de costo
      IF UN_TIPOPLANO = 1 THEN    
          MI_STRSQL := MI_STRSQL || ' AND PERSONAL.ID_CENTRO_DE_COSTO = ''' || UN_CENTROCOSTO || '''';
      --Plano para todos los centros de costo excepto el configurado en el parÃ¡metro    
      ELSIF UN_TIPOPLANO = 2 THEN    
          MI_STRSQL := MI_STRSQL || ' AND PERSONAL.ID_CENTRO_DE_COSTO <> ''' || UN_CENTROCOSTO || '''';
      END IF;

      MI_STRSQL := MI_STRSQL || ' GROUP BY BANCOS_NOMINA.CODIGO_NACHAM,
                             CASE CONCEPTOS.C_TIPO_CUENTA
                                   WHEN ' || '''A''' || ' THEN ' || '''2''' ||
                                 ' WHEN ' || '''C''' || ' THEN ' || '''1''' ||
                                 ' ELSE ''''
                             END,
                             CONCEPTOS.C_CUENTA,
                             TERCERO.NIT,
                             TERCERO.NOMBRE,
                             TERCERO.DIRECCIONEMAIL';

      OPEN RS FOR MI_STRSQL;
      <<RECORREREGISTROS>>
        LOOP
          FETCH RS INTO   MI_CODIGO_NACHAM
                        , MI_TIPO_CUENTA
                        , MI_C_CUENTA
                        , MI_NIT
                        , MI_NOMBRE
                        , MI_DIRECCIONEMAIL
                        , MI_VALOR;
          EXIT WHEN RS%NOTFOUND;
              MI_PLANO := MI_PLANO ||TO_CLOB(RPAD(SUBSTR(MI_NACHAMSUDAMERIS,1,6),6,' ')                                     || -- CODIGO NACHAM BANCOSUDAMERIS
                            RPAD(SUBSTR(MI_CUENTASUDAMERIS,1,16),16,' ')                                                    || -- NUMERO CIENTA SUDAMERIS
                            SUBSTR(MI_TIPOSUDAMERIS,1,1)                                                                    || -- TIPO DE CUENTA DE SUDAMERIS
                            RPAD(SUBSTR(REPLACE(PCK_NOMINA_COM5.FC_QUITAESPECIALES(MI_RAZONSOCIAL),CHR(39),''),1,35),35,' ')|| -- NOMBRE RAZON SOCIAL APORTANTE
                            RPAD(SUBSTR(PCK_NOMINA_COM5.FC_QUITAESPECIALES(UN_DESCRIPCIONDETA),1,45),45,' ')                || -- DESCRIPCION DETALLADA, SE DILIGENCIA DESDE EL FORMULARIO
                            RPAD(SUBSTR(NVL(MI_CODIGO_NACHAM,' '),1,6),6,' ')                                               || -- CODIGO NACHAM DEL BANCO SELECCIONADO
                            RPAD(SUBSTR(MI_TIPO_CUENTA,1,1),1,' ')                                                          || -- TIPO CUENTA DEL BANCO SELECCIONADO
                            RPAD(SUBSTR(NVL(MI_C_CUENTA,' '),1,18),18,' ')                                                  || -- CUENTA DEL BANCO DE DLA PERSONA A PAGAR
                            LPAD(SUBSTR(MI_NIT,1,12),12,' ')                                                                || -- NUMERO DE DOCUMENTO DEL EMPLEADO
                            RPAD(SUBSTR(REPLACE(PCK_NOMINA_COM5.FC_QUITAESPECIALES(MI_NOMBRE),CHR(39),''),1,35),35,' ')     || -- NOMBRE COMPLETO DEL EMPLEADO
                            RPAD(SUBSTR(PCK_NOMINA_COM5.FC_QUITAESPECIALES(UN_DESCRIPCIONPAGO),1,78),78,' ')                || -- DESCRIPCION PAGO, SE DILIGENCIA EN FORMULARIO QUE LLAMA A ESTA FUNCIÃ“N
                            LPAD(SUBSTR(MI_VALOR,1,17),17,' ')                                                              || -- VALOR A PAGAR A EMPLEADO
                            RPAD(SUBSTR(NVL(MI_DIRECCIONEMAIL,' '),1,61),61,' ')                                            || -- CORREO CORPORTATIVO DE EMPLEADO*/
                            RPAD(SUBSTR(PCK_NOMINA_COM5.FC_QUITAESPECIALES(UN_DESCRIPCIONAMPL),1,129),129,' '))             || -- DESCRIPCION AMPLIADA, SE DILIGENCIA EN FORMULARIO QUE LLAMA A ESTA FUNCIÃ“N 
                            CHR(10); 
        END LOOP RECORREREGISTROS;
      CLOSE RS;

    RETURN MI_PLANO;

END FC_PLANOPAGOTERCSUDAMERIS;

