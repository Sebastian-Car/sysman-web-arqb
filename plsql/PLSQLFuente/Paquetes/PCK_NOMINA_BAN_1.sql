                        Se incluye estandarizaciÃ³n de cÃ³digo y manejo de errores                          
    @Name: getDiscoAuditoriaNuevo
*/
  (
    UN_COMPANIA     IN  PCK_SUBTIPOS.TI_COMPANIA,
    UN_CODBANCO     IN  PCK_SUBTIPOS.TI_BANCO_NOM,
    UN_PROCESO      IN  PCK_SUBTIPOS.TI_ID_DE_PROCESO,
    UN_ANIO         IN  PCK_SUBTIPOS.TI_ANIO,
    UN_MES          IN  PCK_SUBTIPOS.TI_MES,
    UN_PERIODO      IN  PCK_SUBTIPOS.TI_PERIODO_NOMI
  )
RETURN CLOB
  AS
    MI_STRSQL         VARCHAR2(3000);    
    MI_CODENTIDAD     VARCHAR2(3000);
    MITIPO_DOC        VARCHAR2(3000);
    MI_NOMBRECOMPLETO VARCHAR2(3000);
    MI_RS             SYS_REFCURSOR;
    MI_DISCONUEVO     CLOB:='';

    RSDOC_IDENTIDAD   PERSONAL.DCTO_IDENTIDAD%TYPE;
    RSNOMBRE          PERSONAL.NOMBRECOMPLETO%TYPE;
    RSCUENTA          PERSONAL.CUENTA%TYPE;
    RSTIPO_CUENTA     PERSONAL.TIPOCUENTA%TYPE;
    RSVALOR           HISTORICOS.VALOR%TYPE;
    RSNUM_DOCUMENTO   PERSONAL.NUMERO_DCTO%TYPE;

  BEGIN
    BEGIN
      SELECT CODIGO_ENTIDAD
      INTO MI_CODENTIDAD
      FROM BANCOS_NOMINA
      WHERE BANCOS_NOMINA.COMPANIA = UN_COMPANIA
        AND BANCOS_NOMINA.BANCO = UN_CODBANCO;

      EXCEPTION WHEN NO_DATA_FOUND THEN
        MI_CODENTIDAD:='051';
    END;
    MI_CODENTIDAD:=PCK_SYSMAN_UTL.FC_STRZERO(UN_NUMERO => MI_CODENTIDAD, UN_LONGITUD=>  3);
    MI_DISCONUEVO:= MI_DISCONUEVO 
                 || MI_CODENTIDAD 
                 || chr(10);
    OPEN MI_RS FOR SELECT PERSONAL.DCTO_IDENTIDAD,
                           PERSONAL.NOMBRECOMPLETO,
                           PERSONAL.CUENTA,
                           PERSONAL.TIPOCUENTA,
                           HISTORICOS.VALOR,
                           PERSONAL.NUMERO_DCTO
                    FROM HISTORICOS INNER JOIN PERSONAL 
                      ON HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
                     AND HISTORICOS.COMPANIA = PERSONAL.COMPANIA 
                    INNER JOIN BANCOS_NOMINA 
                      ON PERSONAL.BANCO = BANCOS_NOMINA.BANCO 
                     AND PERSONAL.COMPANIA = BANCOS_NOMINA.COMPANIA
                    WHERE HISTORICOS.COMPANIA      = UN_COMPANIA
                      AND PERSONAL.BANCO           = UN_CODBANCO
                      AND PERSONAL.ID_DE_EMPLEADO  <> 0 
                      AND HISTORICOS.ID_DE_PROCESO = UN_PROCESO
                      AND HISTORICOS.ANO           = UN_ANIO 
                      AND HISTORICOS.MES           = UN_MES
                      AND HISTORICOS.PERIODO       = UN_PERIODO 
                      AND HISTORICOS.ID_DE_CONCEPTO='144'
                    ORDER BY PERSONAL.NOMBRECOMPLETO;           
     LOOP
      FETCH MI_RS INTO RSDOC_IDENTIDAD, RSNOMBRE, RSCUENTA, RSTIPO_CUENTA, RSVALOR, RSNUM_DOCUMENTO;
      EXIT WHEN MI_RS%NOTFOUND;
      CASE RSDOC_IDENTIDAD
        WHEN 'C' THEN MITIPO_DOC:= '1';
        WHEN 'E' THEN MITIPO_DOC:= '6';
        WHEN 'T' THEN MITIPO_DOC:= '4';
        WHEN 'R' THEN MITIPO_DOC:= 'R';
        WHEN 'U' THEN MITIPO_DOC:= '8';
        END CASE;

      MI_NOMBRECOMPLETO:= REPLACE(RSNOMBRE, '-', '');
      MI_NOMBRECOMPLETO:= REPLACE(MI_NOMBRECOMPLETO, ''||CHR(209), 'N');
      MI_NOMBRECOMPLETO:= REPLACE(MI_NOMBRECOMPLETO, ''||CHR(241), 'n');
      MI_NOMBRECOMPLETO:= REPLACE(MI_NOMBRECOMPLETO, ''||CHR(193), 'A');
      MI_NOMBRECOMPLETO:= REPLACE(MI_NOMBRECOMPLETO, ''||CHR(201), 'E');
      MI_NOMBRECOMPLETO:= REPLACE(MI_NOMBRECOMPLETO, ''||CHR(205), 'I');
      MI_NOMBRECOMPLETO:= REPLACE(MI_NOMBRECOMPLETO, ''||CHR(211), 'O');
      MI_NOMBRECOMPLETO:= REPLACE(MI_NOMBRECOMPLETO, ''||CHR(218), 'U');
      MI_NOMBRECOMPLETO:= REPLACE(MI_NOMBRECOMPLETO, ''||CHR(40), '');
      MI_NOMBRECOMPLETO:= REPLACE(MI_NOMBRECOMPLETO, ''||CHR(41), '');
      MI_DISCONUEVO:= MI_DISCONUEVO
                   || MI_CODENTIDAD
                   || RPAD(RSNUM_DOCUMENTO, 15, ' ')
                   || MITIPO_DOC
                   || RPAD(RSCUENTA, 17, ' ');

      IF(RSTIPO_CUENTA = 'A') THEN
        MI_DISCONUEVO:=MI_DISCONUEVO||'1';
      ELSE
        MI_DISCONUEVO:=MI_DISCONUEVO||'0';
      END IF;
      MI_DISCONUEVO:= MI_DISCONUEVO
                   || RPAD(MI_NOMBRECOMPLETO, 30, ' ')
                   || PCK_SYSMAN_UTL.FC_STRZERO(UN_NUMERO => RSVALOR, UN_LONGITUD => 13)
                   || '.00999'
                   || RPAD(' ', 50, ' ')
                   || RPAD(' ', 40, ' ')
                   || chr(10);        
      END LOOP;
      CLOSE MI_RS;

  RETURN MI_DISCONUEVO;   
END FC_DISCOAUDITORIA_NUEVO;


