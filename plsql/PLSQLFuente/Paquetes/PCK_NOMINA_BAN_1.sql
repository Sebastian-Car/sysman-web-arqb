FUNCTION FC_DISCODAVIVIENDA
 /*
    NAME              : FC_DISCODAVIVIENDA ----> NOMBRE EN ACCESS Discodavivienda
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : EDGAR LEONARDO SARMIENTO
    DATE MIGRADOR     : 16/09/2015
    TIME              : 10:07 PM
    SOURCE MODULE     : NOMINAP2015.04.02 UNIFICADAS MPV 11052015_MPV - 185  - WEB para migrar final  4TO envio.accdb
    MODIFIER          : JOSÃ‰ PASCUAL GÃ“MEZ
    DATE MODIFIED     : 05/09/2017
    TIME              : 13:07 PM
    DESCRIPTION       : FUNCION MIGRADA QUE PERMITE GENERAR EL DISCO DE BANCOS PARA DAVIVIENDA
                        Se incluye estandarizaciÃ³n de cÃ³digo y manejo de errores  
    @Name: getDiscoDavivienda
  */
  (
    UN_COMPANIA     IN  PCK_SUBTIPOS.TI_COMPANIA,
    UN_CONSECUTIVO  IN  PCK_SUBTIPOS.TI_ENTERO,
    UN_CODIGOBANCO  IN  PCK_SUBTIPOS.TI_BANCO_NOM,
    UN_CODOFICINA   IN  VARCHAR2,
    UN_PROCESO      IN  PCK_SUBTIPOS.TI_ID_DE_PROCESO,
    UN_ANIO         IN  PCK_SUBTIPOS.TI_ANIO,
    UN_MES          IN  PCK_SUBTIPOS.TI_MES,
    UN_PERIODO      IN  PCK_SUBTIPOS.TI_PERIODO_NOMI,
    UN_FECHA        IN  DATE
  )
RETURN CLOB
  AS
    MI_NIT              VARCHAR2(3000);
    MI_CUENTA           VARCHAR2(3000);
    MI_NUM_EMPLEADOS    PCK_SUBTIPOS.TI_ENTERO:=0;
    MI_TOTALPAGO        PCK_SUBTIPOS.TI_DOBLE:=0;
    MI_TIPO_CUENTA      VARCHAR2(3000);
    MI_TIPO_NIT         VARCHAR2(3000);
    MI_CODENTIDAD       VARCHAR2(3000);
    MI_DISCODAVIVIENDA  CLOB;
    MI_RS                SYS_REFCURSOR;
    RS_NUMDOCUMENTO     PERSONAL.NUMERO_DCTO%TYPE;
    RSNIT               PARAMETROS_DE_ENTRADA.NIT%TYPE;
    RSTIPO_NIT          PARAMETROS_DE_ENTRADA.TIPONIT%TYPE;
    RSCUENTA            BANCOS_NOMINA.CUENTA%TYPE;
    RSTIPO_CUENTA       BANCOS_NOMINA.TIPO_CUENTA%TYPE;
    RSVALOR             HISTORICOS.VALOR%TYPE;
  BEGIN
    <<TOTALES>>
    OPEN MI_RS FOR SELECT PARAMETROS_DE_ENTRADA.NIT, 
                           PARAMETROS_DE_ENTRADA.TIPONIT, 
                           BANCOS_NOMINA.CUENTA, 
                           BANCOS_NOMINA.TIPO_CUENTA, 
                           HISTORICOS.VALOR 
                    FROM HISTORICOS INNER JOIN PERSONAL 
                      ON HISTORICOS.COMPANIA       = PERSONAL.COMPANIA 
                     AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
                    INNER JOIN BANCOS_NOMINA 
                      ON PERSONAL.COMPANIA = BANCOS_NOMINA.COMPANIA 
                     AND PERSONAL.BANCO    = BANCOS_NOMINA.BANCO 
                    LEFT JOIN PARAMETROS_DE_ENTRADA 
                       ON HISTORICOS.COMPANIA       = PARAMETROS_DE_ENTRADA.COMPANIA
                    WHERE HISTORICOS.COMPANIA       = UN_COMPANIA
                      AND HISTORICOS.ID_DE_PROCESO  = UN_PROCESO
                      AND HISTORICOS.ANO            = UN_ANIO
                      AND HISTORICOS.MES            = UN_MES
                      AND HISTORICOS.PERIODO        = UN_PERIODO 
                      AND HISTORICOS.ID_DE_CONCEPTO = '144' 
                      AND PERSONAL.ID_DE_EMPLEADO   <> 0
                      AND PERSONAL.BANCO            = UN_CODIGOBANCO;
    LOOP
      FETCH MI_RS INTO RSNIT, RSTIPO_NIT, RSCUENTA, RSTIPO_CUENTA, RSVALOR;
      EXIT WHEN MI_RS%NOTFOUND;
      IF(MI_NUM_EMPLEADOS=0) THEN
        MI_NIT    :=  REPLACE(RSNIT, '-','');
        MI_CUENTA :=  REPLACE(RSCUENTA, '-', '');
        IF(RSTIPO_CUENTA = 'C') THEN
          MI_TIPO_CUENTA:='CC';
        ELSE
          MI_TIPO_CUENTA:='CA';
        END IF;
        IF(RSTIPO_NIT = 'N') THEN
          MI_TIPO_NIT:='01';
        ELSE
          MI_TIPO_NIT:='02';
        END IF;
      END IF;
      MI_NUM_EMPLEADOS :=MI_NUM_EMPLEADOS+1;
      MI_TOTALPAGO     :=MI_TOTALPAGO+ROUND(RSVALOR, 0);
    END LOOP TOTALES;
    CLOSE MI_RS;
    BEGIN
      SELECT CODIGO_ENTIDAD
      INTO MI_CODENTIDAD
      FROM BANCOS_NOMINA
      WHERE BANCOS_NOMINA.COMPANIA = UN_COMPANIA
        AND BANCOS_NOMINA.BANCO    = UN_CODIGOBANCO;
    EXCEPTION WHEN NO_DATA_FOUND THEN
      MI_CODENTIDAD:='NULL';
    END;
    MI_CODENTIDAD      :=PCK_SYSMAN_UTL.FC_STRZERO(UN_NUMERO   => MI_CODENTIDAD, 
                                                   UN_LONGITUD => 6);
    MI_DISCODAVIVIENDA :=MI_DISCODAVIVIENDA || MI_CODENTIDAD || chr(10);
    MI_NIT:= REPLACE(MI_NIT,'.','');
    MI_NIT:= REPLACE(MI_NIT,'-', '');
    MI_NIT:= REPLACE(MI_NIT,',', '');

    MI_DISCODAVIVIENDA := MI_DISCODAVIVIENDA 
                       || 'RC' 
                       || PCK_SYSMAN_UTL.FC_STRZERO(UN_NUMERO => MI_NIT, UN_LONGITUD => 16)
                       || 'NOMI'
                       || PCK_SYSMAN_UTL.FC_STRZERO(UN_NUMERO => UN_CONSECUTIVO, UN_LONGITUD => 4)
                       || PCK_SYSMAN_UTL.FC_STRZERO(UN_NUMERO => MI_CUENTA, UN_LONGITUD => 16)
                       || LPAD(MI_TIPO_CUENTA, 2, '0')
                       || MI_CODENTIDAD
                       || PCK_SYSMAN_UTL.FC_STRZERO(UN_NUMERO => MI_TOTALPAGO, UN_LONGITUD => 16)
                       || '00'
                       || PCK_SYSMAN_UTL.FC_STRZERO(UN_NUMERO => MI_NUM_EMPLEADOS, UN_LONGITUD => 6)
                       || TO_CHAR(UN_FECHA, 'YYYYMMdd')
                       || RPAD('0', 6, '0')
                       || RPAD('0', 4, '0')
                       || '9999'
                       || RPAD('0', 8, '0')
                       || RPAD('0', 6, '0')
                       || RPAD('0', 2, '0') 
                       || RPAD(MI_TIPO_NIT, 2, '0')
                       || RPAD('0', 12, '0')
                       || PCK_SYSMAN_UTL.FC_STRZERO(UN_NUMERO => UN_CODOFICINA, UN_LONGITUD => 4)
                       || RPAD('0',4, '0')
                       || RPAD('0', 36, '0');
    MI_DISCODAVIVIENDA:=MI_DISCODAVIVIENDA||chr(10);

    <<PERSONAS>>
    OPEN MI_RS FOR  SELECT PERSONAL.TIPOCUENTA,
                             HISTORICOS.VALOR, 
                             PERSONAL.NUMERO_DCTO, 
                             PERSONAL.CUENTA
                      FROM HISTORICOS INNER JOIN PERSONAL 
                        ON HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO 
                       AND HISTORICOS.COMPANIA = PERSONAL.COMPANIA
                      INNER JOIN BANCOS_NOMINA 
                        ON PERSONAL.BANCO = BANCOS_NOMINA.BANCO 
                       AND PERSONAL.COMPANIA = BANCOS_NOMINA.COMPANIA
                      LEFT JOIN PARAMETROS_DE_ENTRADA 
                        ON HISTORICOS.COMPANIA = PARAMETROS_DE_ENTRADA.COMPANIA
                      WHERE HISTORICOS.COMPANIA       = UN_COMPANIA
                        AND HISTORICOS.ID_DE_PROCESO  = UN_PROCESO
                        AND HISTORICOS.ANO            = UN_ANIO
                        AND HISTORICOS.MES            = UN_MES
                        AND HISTORICOS.PERIODO        = UN_PERIODO 
                        AND HISTORICOS.ID_DE_CONCEPTO = '144' 
                        AND PERSONAL.ID_DE_EMPLEADO   <> 0
                        AND PERSONAL.BANCO            = UN_CODIGOBANCO
                      ORDER BY PERSONAL.TIPOCUENTA, PERSONAL.NOMBRECOMPLETO;
    LOOP
      FETCH MI_RS INTO RSTIPO_CUENTA, RSVALOR, RS_NUMDOCUMENTO, RSCUENTA;
      EXIT WHEN MI_RS%NOTFOUND;
      MI_DISCODAVIVIENDA:= MI_DISCODAVIVIENDA
                        || 'TR'
                        || PCK_SYSMAN_UTL.FC_STRZERO(UN_NUMERO => RS_NUMDOCUMENTO, UN_LONGITUD => 16)
                        || RPAD('0', 16, '0')
                        || PCK_SYSMAN_UTL.FC_STRZERO(UN_NUMERO => RSCUENTA, UN_LONGITUD => 16);
      IF(RSTIPO_CUENTA = 'A') THEN
        MI_DISCODAVIVIENDA:= MI_DISCODAVIVIENDA ||'CA';
      ELSE
        MI_DISCODAVIVIENDA:= MI_DISCODAVIVIENDA ||'CC';
      END IF;
      MI_DISCODAVIVIENDA:= MI_DISCODAVIVIENDA
                        || MI_CODENTIDAD
                        || PCK_SYSMAN_UTL.FC_STRZERO(UN_NUMERO => RSVALOR, UN_LONGITUD => 16)
                        || '00'
                        || RPAD('0', 6, '0')
                        || '0209999'
                        || RPAD('0', 40, '0')
                        || RPAD('0', 18, '0')
                        || RPAD('0', 8, '0')
                        || RPAD('0', 8, '0')
                        || RPAD('0', 7, '0');
