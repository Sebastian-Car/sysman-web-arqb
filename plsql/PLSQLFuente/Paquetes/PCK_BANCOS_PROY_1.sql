  FUNCTION FC_INSERTARVIGENCIANUEVA
  /*
      NAME              : FC_INSERTARVIGENCIANUEVA
      AUTHORS           : SYSMAN  SAS
      AUTHOR MIGRACION  : LEYDI MILENA CORTES FORERO
      DATE MIGRADOR     : 18/08/2015
      TIME              : 3:22 PM
      SOURCE MODULE     : BancoProyectos
      MODIFIER          : YEISSON ALEJANDRO ROJAS RUIZ
      DATE MODIFIED     : 31/01/2017
      TIME              : 08:10 AM
      DESCRIPTION       : PERMITE ACTUALIZAR UNA VIGENCIA Y RETORNAR EL NÃ?MERO DE FILAS QUE HA SIDO ACTUALIZADAS EN EL REGISTRO PARA DICHA VIGENCIA
      PARAMETERS        : UN_COMPANIA => COMPANIA EN LA QUE SE ESTA TRABAJANDO.
                          UN_ANIO_INI => AÑO POR EL QUE SE VA A FILTRAR EN LA CONSULTA.
                          UN_ANIO_FIN => AÑO QUE SE VA A INSERTAR EN LOS NUEVOS REGISTROS.
                          UN_OPCION   => CADENA QUE DETERMINA LA ACCION A REALIZAR.
      MODIFICATIONS     : CORRECCIÓN SEGUN ESTANDAR DE PROGRAMACION Y OPTIMIZACION DEL MANEJO DE ERRORES.
     @NAME: insertarVigencias
    */
  (
    UN_COMPANIA  IN PCK_SUBTIPOS.TI_COMPANIA,
    UN_ANIO_INI  IN PCK_SUBTIPOS.TI_ANIO,
    UN_ANIO_FIN  IN PCK_SUBTIPOS.TI_ANIO,
    UN_OPCION    IN VARCHAR2, -- DETERMINA SI EL FORMULARIO ACTUALIZAR VIGENCIA ES LLAMADO DESDE EL FORMULARIO NIVEL_CUMPLIMIENTO O DESDE EL FORMULARIO FRM_NIV_PLAN_IND.
    UN_USUARIO   IN PCK_SUBTIPOS.TI_USUARIO 
  )
  RETURN NUMBER AS
    MI_CAMPOS     PCK_SUBTIPOS.TI_CAMPOS;
    MI_VALORES    PCK_SUBTIPOS.TI_VALORES;
    MI_EXCLUIDOS  VARCHAR2(100); 
  BEGIN
    BEGIN
      MI_EXCLUIDOS:= 'VIGENCIA,MODIFIED_BY,DATE_MODIFIED,CREATED_BY,DATE_CREATED';
      IF UN_OPCION = 'NivPlan' THEN
        MI_CAMPOS        :=  PCK_SYSMAN_UTL.FC_LISTA_CAMPOS(UN_TABLA     => 'BP_NIVEL_PLAN_IND',
                                                            UN_EXCLUIDOS => MI_EXCLUIDOS);
        MI_VALORES       := 'SELECT ' || UN_ANIO_FIN || ' VIGENCIA,
                                    ' || MI_CAMPOS || '
                                    ,SYSDATE
                                    ,'''||UN_USUARIO||'''
                            FROM     BP_NIVEL_PLAN_IND
                            WHERE    COMPANIA IN (''' || UN_COMPANIA ||  ''')
                              AND    VIGENCIA IN ('   || UN_ANIO_INI ||  ')';
        MI_CAMPOS        := 'VIGENCIA,' || MI_CAMPOS || ',DATE_CREATED
                                                         ,CREATED_BY';

        PCK_DATOS.GL_RTA := PCK_DATOS.FC_ACME(UN_TABLA   => 'BP_NIVEL_PLAN_IND',
                                              UN_ACCION  => 'IS',
                                              UN_CAMPOS  => MI_CAMPOS,
                                              UN_VALORES => MI_VALORES);

      ELSIF UN_OPCION = 'NivCum' THEN

        MI_CAMPOS        := PCK_SYSMAN_UTL.FC_LISTA_CAMPOS(UN_TABLA     => 'BP_NIVEL_CUMPLIMIENTO',
                                                           UN_EXCLUIDOS => MI_EXCLUIDOS);
        MI_VALORES       := 'SELECT ' || UN_ANIO_FIN || ' VIGENCIA,
                                    ' || MI_CAMPOS || '
                                    ,SYSDATE
                                    ,'''||UN_USUARIO||'''
                             FROM   BP_NIVEL_CUMPLIMIENTO
                             WHERE  COMPANIA IN (''' || UN_COMPANIA || ''')
                              AND   VIGENCIA IN ('   || UN_ANIO_INI || ')';
        MI_CAMPOS        := 'VIGENCIA,' || MI_CAMPOS|| ',DATE_CREATED
                                                         ,CREATED_BY';

        PCK_DATOS.GL_RTA := PCK_DATOS.FC_ACME(UN_TABLA   => 'BP_NIVEL_CUMPLIMIENTO',
                                              UN_ACCION  => 'IS',
                                              UN_CAMPOS  => MI_CAMPOS,
                                              UN_VALORES => MI_VALORES);
      END IF;

      EXCEPTION WHEN PCK_EXCEPCIONES.EXC_INSERTAR THEN
        RAISE PCK_EXCEPCIONES.EXC_BANCO_PROYECTOS;
    END;
    RETURN PCK_DATOS.GL_RTA;

    EXCEPTION WHEN PCK_EXCEPCIONES.EXC_BANCO_PROYECTOS THEN
      PCK_ERR_MSG.RAISE_WITH_MSG(
        UN_EXC_COD =>SQLCODE,
        UN_ERROR_COD=>PCK_ERRORES.ER_BANCO_NUEVA_VIGENCIA_INSERT
      );

  END FC_INSERTARVIGENCIANUEVA;

