                          UN_USUARIO  => USUARIO ACTUAL.
      MODIFICATIONS     : CORRECCIÓN SEGUN ESTANDAR DE PROGRAMACION Y OPTIMIZACION DEL MANEJO DE ERRORES.
      @NAME: anularSolicitudesBancoProyectoRes
    */
    UN_FECHA          IN    DATE,
    UN_COMPANIA       IN    PCK_SUBTIPOS.TI_COMPANIA,
    UN_TIPOT          IN    BPNOVEDADPROYECTO.TIPOT%TYPE,
    UN_MODULO         IN    PCK_SUBTIPOS.TI_MODULO,
    UN_USUARIO        IN    PCK_SUBTIPOS.TI_USUARIO
  )
  RETURN CLOB AS
    MI_NOVANULADA         CLOB;
    MI_NOVEDADSINANULAR   CLOB;
    MI_RESUMEN            CLOB;
    MI_RTA                VARCHAR2(10 CHAR);
    MI_CONSULTA           PCK_SUBTIPOS.TI_STRSQL;
    MI_NOMBRETIPO         BPTIPONOVEDAD.NOMBRE%TYPE;
    MI_COMPANIA           BPNOVEDADPROYECTO.COMPANIA%TYPE;
    MI_TIPOT              BPNOVEDADPROYECTO.TIPOT%TYPE;
    MI_CLASET             BPNOVEDADPROYECTO.CLASET%TYPE;
    MI_CODIGO             BPNOVEDADPROYECTO.CODIGO%TYPE;
    MI_DEPENDENCIA        BPNOVEDADPROYECTO.DEPENDENCIA%TYPE;
    RS_ANULADA            SYS_REFCURSOR;
  BEGIN
    MI_CONSULTA := 'SELECT COMPANIA,
                           TIPOT,
                           CLASET,
                           CODIGO,
                           DEPENDENCIA
                      FROM BPNOVEDADPROYECTO
                     WHERE COMPANIA   IN(''' || UN_COMPANIA || ''')
                       AND TIPOT      IN(''' || UN_TIPOT || ''')
                       AND ESTADO     NOT IN(''A'')
                       AND FECHA_VOBO <= TO_DATE(''' || UN_FECHA || ''',''DD/MM/YY'')
                     ORDER BY CODIGO ';

    BEGIN
      BEGIN
        SELECT DISTINCT NOMBRE
          INTO MI_NOMBRETIPO
          FROM BPTIPONOVEDAD
         WHERE TIPOT = UN_TIPOT;

        EXCEPTION WHEN NO_DATA_FOUND THEN
          RAISE PCK_EXCEPCIONES.EXC_BANCO_PROYECTOS;
      END;
      EXCEPTION WHEN PCK_EXCEPCIONES.EXC_BANCO_PROYECTOS THEN
        PCK_ERR_MSG.RAISE_WITH_MSG(
          UN_EXC_COD =>SQLCODE,
          UN_ERROR_COD=>PCK_ERRORES.ER_BANCO_RESU_ANUL_NOMBRE
        );
    END;

    <<NOVEDADES>>
    OPEN RS_ANULADA FOR MI_CONSULTA;
      LOOP
        FETCH RS_ANULADA
         INTO MI_COMPANIA,
              MI_TIPOT,
              MI_CLASET,
              MI_CODIGO,
              MI_DEPENDENCIA;
        EXIT WHEN RS_ANULADA%NOTFOUND;
        IF MI_COMPANIA IS NULL AND MI_TIPOT IS NULL THEN
           MI_RESUMEN := NULL;
        END IF;
        MI_RTA := PCK_BANCOS_PROY.FC_ANULAR_SOLICITUD(UN_COMPANIA    => UN_COMPANIA,
                                                      UN_TIPOT       => MI_TIPOT,
                                                      UN_CLASET      => MI_CLASET,
                                                      UN_CODIGO      => MI_CODIGO,
                                                      UN_DEPENDENCIA => MI_DEPENDENCIA,
                                                      UN_MODULO      => UN_MODULO,
                                                      UN_USUARIO     => UN_USUARIO);
        MI_RTA := NVL(MI_RTA, 'SIN ANULAR');
        IF MI_RTA = 'ANULADA' THEN
          MI_NOVANULADA       := MI_NOVANULADA || ' '
                                 || MI_TIPOT || ' - '
                                 || MI_CLASET || '-'
                                 || RPAD(MI_CODIGO, 15, ' ') || CHR(13);
        ELSE
          MI_NOVEDADSINANULAR := MI_NOVEDADSINANULAR || ' '
                                 || MI_TIPOT || ' - '
                                 || MI_CLASET || '-'
                                 || RPAD(MI_CODIGO, 15, ' ') ||'-'
                                 || MI_RTA || CHR(13);
        END IF;
      END LOOP NOVEDADES;
    CLOSE RS_ANULADA;

    MI_RESUMEN := 'RESUMEN DE ANULACION PARA LAS NOVEDADES '
                  || MI_NOMBRETIPO || CHR(13)
                  || ' CON FECHA DE CORTE PARA ANULACIÓN '
                  || REPLACE(REGEXP_REPLACE(TO_CHAR(UN_FECHA, 'Day, DD "de" MONTH "de" YYYY'),'( ){2,}', ' '),' ,',',') || CHR(13) || CHR(13)
                  || ' NOVEDADES ANULADAS ' || CHR(13)
                  || MI_NOVANULADA || CHR(13) || CHR(13)
                  || ' NOVEDADES SIN ANULAR ' || CHR(13)
                  || MI_NOVEDADSINANULAR;

    IF MI_NOVANULADA IS NULL AND MI_NOVEDADSINANULAR IS NULL THEN
      MI_RESUMEN := 'NOANULADA';
    END IF;

    RETURN MI_RESUMEN;
  END FC_RESUMENANULACION;

