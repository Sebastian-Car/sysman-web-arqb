PROCEDURE PR_ARMONIZARPD(
    /*
    NAME              : Procedimiento Boton Aceptar-> En Access: Aceptar_Click()
    AUTHORS           : STEFANINI SYSMAN
    AUTHOR MIGRATION  : JHON FREDY HERNANDEZ CASTRO
    DATE MIGRATION    : 13/03/2018
    TIME              : 08:10 AM
    SOURCE MODULE     : BANCO DE PROYECTOS/ MANTENIMIENTO / ARMONIZAR PLAN DE DESARROLLO (SysmanBP2018.02.02)
    DESCRIPTION       : Valida la existencia de valores con respecto a una compañia,proyecto,rubro y vigencia,
    si no existen registros con respecto a estos valores, lo registra en la tabla BP_PROYECTOSRUBROS
    MODIFIED BY       : GUSTAVO ANDRÉS FIGUEREDO AVILA
    DATE			  : 02/06/2021
    TIME			  : 12:47 PM
    SOURCE MODULE     : BANCO DE PROYECTOS/ MANTENIMIENTO / ARMONIZAR PLAN DE DESARROLLO (Web)
    DESCRIPTION       : Se añade a la validación sobre la existencia de registro en la tabla BP_PROYECTOSRUBROS,
    el campo FUENTERECURSOSRUBRO.
	MODIFIED BY       : GUSTAVO ANDRÉS FIGUEREDO AVILA
    DATE			  : 14/01/2022
    TIME			  : 14:49
    SOURCE MODULE     : BANCO DE PROYECTOS/ MANTENIMIENTO / ARMONIZAR PLAN DE DESARROLLO (Web)
    DESCRIPTION       : Se ajusta la asignación de los valores para sector, programa y RUBROPPTALES,
	los cuales se enviaban como entero, pero ahora contienen el carater '.'
    PARAMETERS        : UN_COMPANIA => COMPANIA EN LA QUE SE ESTÁ TRABAJANDO.
    UN_VIGENCIA => UN AÑO DE VIGENCIA INICIAL
    @NAME  : armonizarPd
    @METHOD: POST
    MODIFIED BY       : LUIS JACOBO DIAZ MUÑOZ
    DATE        : 01/03/2022
    TIME        : 12:47 PM
    SOURCE MODULE     : BANCO DE PROYECTOS/ MANTENIMIENTO / ARMONIZAR PLAN DE DESARROLLO (ARQ-B)
    DESCRIPTION       : SE AÑADE LOS CAMPOS CENRTOCOSTO, REFERENCIA, AUXILIAR A LA CONSULTA PRINCIPAL 
    Y SE AÑADEN FILTROS DENTRO DEL LOOP QUE CONSULTARAN DE ACUERDO A LOS CAMPOS NOMBRADOS

    */
    UN_COMPANIA IN PCK_SUBTIPOS.TI_COMPANIA,
    UN_VIGENCIA IN PCK_SUBTIPOS.TI_ANIO 
  )
AS
  MI_TABLA PCK_SUBTIPOS.TI_TABLA;
  MI_FILAS PCK_SUBTIPOS.TI_ENTERO;
  MI_CAMPOS PCK_SUBTIPOS.TI_CAMPOS;
  MI_VALORES PCK_SUBTIPOS.TI_VALORES;
  MI_EXISTPROYRUB PCK_SUBTIPOS.TI_NUMORDEN;
BEGIN
  <<ARMONIZACIONPD>>
  FOR MI_RS IN
    ( 
    SELECT  BP_ARMONIZACIONPD.COMPANIA,
              BP_ARMONIZACIONPD.VIGENCIA_PLAN,
              BP_ARMONIZACIONPD.VIGENCIA_META,
              BP_ARMONIZACIONPD.ID_META,
              BP_ARMONIZACIONPD.VIGENCIA_RUBRO,
              BP_ARMONIZACIONPD.ID_RUBRO,
              BP_ARMONIZACIONPD.FUENTE,              
              BP_ARMONIZACIONPD.CENTROCOSTO,
              BP_ARMONIZACIONPD.REFERENCIA,
              BP_ARMONIZACIONPD.AUXILIARGENERAL,
              PLAN_PRESUPUESTAL.CODIGO,
              BP_PROYECTO_PLAN_INDICATIVO.PROYECTO
      FROM BP_ARMONIZACIONPD
        INNER JOIN BP_PROYECTO_PLAN_INDICATIVO
          ON BP_ARMONIZACIONPD.COMPANIA         = BP_PROYECTO_PLAN_INDICATIVO.COMPANIA
          AND BP_ARMONIZACIONPD.ID_META         = BP_PROYECTO_PLAN_INDICATIVO.ID_PLAN_P
          AND BP_ARMONIZACIONPD.VIGENCIA_PLAN   = BP_PROYECTO_PLAN_INDICATIVO.VIGENCIA_PLAN_P
          AND BP_ARMONIZACIONPD.VIGENCIA_META   = BP_PROYECTO_PLAN_INDICATIVO.VIGENCIA_META_P
        INNER JOIN PLAN_PRESUPUESTAL
          ON BP_ARMONIZACIONPD.COMPANIA         = PLAN_PRESUPUESTAL.COMPANIA
          AND BP_ARMONIZACIONPD.VIGENCIA_RUBRO  = PLAN_PRESUPUESTAL.ANO
          AND BP_ARMONIZACIONPD.ID_RUBRO        = PLAN_PRESUPUESTAL.CODIGO
      WHERE BP_ARMONIZACIONPD.COMPANIA          = UN_COMPANIA
        AND BP_ARMONIZACIONPD.VIGENCIA_META     = UN_VIGENCIA
    )
  LOOP
    BEGIN
      SELECT DISTINCT 'X'
      INTO MI_EXISTPROYRUB
      FROM BP_PROYECTOSRUBROS
      WHERE BP_PROYECTOSRUBROS.COMPANIA     = MI_RS.COMPANIA
        AND BP_PROYECTOSRUBROS.PROYECTO     = MI_RS.PROYECTO
        AND BP_PROYECTOSRUBROS.RUBROPPTALES = MI_RS.ID_RUBRO
        AND BP_PROYECTOSRUBROS.VIGENCIA     = MI_RS.VIGENCIA_RUBRO
        AND BP_PROYECTOSRUBROS.FUENTERECURSOSRUBRO  = MI_RS.FUENTE
        AND BP_PROYECTOSRUBROS.CENTRO_COSTO = MI_RS.CENTROCOSTO
        AND BP_PROYECTOSRUBROS.REFERENCIA = MI_RS.REFERENCIA
        AND BP_PROYECTOSRUBROS.AUXILIAR = MI_RS.AUXILIARGENERAL;

    EXCEPTION
    WHEN NO_DATA_FOUND THEN
      DECLARE
        MI_MSGERROR PCK_SUBTIPOS.TI_CLAVEVALOR;
      BEGIN
        MI_TABLA   := 'BP_PROYECTOSRUBROS';
        MI_CAMPOS  := 'COMPANIA                                                                                      
                      ,PROYECTO                                                                                          
                      ,DIMENSION                                                                                          
                      ,SECTOR                                                                                          
                      ,PROGRAMA                                                                                          
                      ,SUBPROGRAMA                                                                                          
                      ,FUENTERECURSOSRUBRO                                                                                          
                      ,VIGENCIA                                                                                          
                      ,RUBROPPTALES
                      ,CENTRO_COSTO
                      ,REFERENCIA
                      ,AUXILIAR
                       ';
        MI_VALORES := ' '''|| MI_RS.COMPANIA || '''                                                                                          
                      ,'|| MI_RS.PROYECTO ||'                                                                                          
                      ,'''|| MI_RS.CODIGO ||'''                                                                                          
                      ,'''|| MI_RS.ID_RUBRO ||'''                                                                                          
                      ,'''|| MI_RS.ID_RUBRO ||'''                                                                                          
                      ,'''|| MI_RS.ID_RUBRO ||'''                                                                                          
                      ,'''|| MI_RS.FUENTE ||'''                                                                                          
                      ,'''|| MI_RS.VIGENCIA_RUBRO ||'''                                                                                          
                      ,'''|| MI_RS.ID_RUBRO ||'''
                      ,'''|| MI_RS.CENTROCOSTO ||'''   
                      ,'''|| MI_RS.REFERENCIA ||'''   
                      ,'''|| MI_RS.AUXILIARGENERAL ||'''';
        BEGIN
          MI_FILAS := PCK_DATOS.FC_ACME(UN_TABLA => MI_TABLA, 
                                        UN_ACCION => 'I', 
                                        UN_CAMPOS => MI_CAMPOS, 
                                        UN_VALORES => MI_VALORES);
        EXCEPTION WHEN PCK_EXCEPCIONES.EXC_INSERTAR THEN
          RAISE PCK_EXCEPCIONES.EXC_BANCO_PROYECTOS;
        END;
      EXCEPTION WHEN PCK_EXCEPCIONES.EXC_BANCO_PROYECTOS THEN
        MI_MSGERROR(1).CLAVE := 'VALORES';
        MI_MSGERROR(2).VALOR := MI_VALORES;
        PCK_ERR_MSG.RAISE_WITH_MSG( 
          UN_EXC_COD => SQLCODE, 
          UN_ERROR_COD => PCK_ERRORES.ERR_BANCOP_INSBPPROYECTRUB, 
          UN_TABLAERROR => MI_TABLA, 
          UN_REEMPLAZOS => MI_MSGERROR);
      END;
    END;
  END LOOP ARMONIZACIONPD;
END PR_ARMONIZARPD;

