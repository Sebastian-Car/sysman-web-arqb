                          PROGRAMACION.CODIGOACTIVIDAD,
                          (NVL(VALOR1,0) + NVL(VALOR2,0) + NVL(VALOR3,0) + NVL(VALOR4,0)
                            + NVL(VALOR5,0) + NVL(VALOR6,0) + NVL(VALOR7,0) + NVL(VALOR8,0)
                            + NVL(VALOR9,0) + NVL(VALOR10,0) + NVL(VALOR11,0) + NVL(VALOR12,0)) VALORPROGRAMADOCOM
                  FROM    PROGRAMACION
                    INNER JOIN PROYECTOS
                      ON  PROGRAMACION.COMPANIA       = PROYECTOS.COMPANIA
                      AND PROGRAMACION.CODIGOPROYECTO = PROYECTOS.CODIGO
                  WHERE   PROGRAMACION.COMPANIA = UN_COMPANIA
                    AND   PROYECTOS.VIGENCIAINICIO = UN_VIGENCIA
                    AND   PROGRAMACION.TIPOESTADO IN ('P', 'RP')) PROG
          ON  COMPONENTES.COMPANIA = PROG.COMPANIA
          AND COMPONENTES.CODIGOPROYECTO = PROG.CODIGOPROYECTO
          AND COMPONENTES.TIPOCOMPONENTE = PROG.TIPOCOMPONENTE
          AND COMPONENTES.CODIGO = PROG.CODIGOCOMPONENTE
      WHERE   COMPONENTES.COMPANIA       = UN_COMPANIA
        AND   COMPONENTES.CODIGOPROYECTO BETWEEN UN_PROYECTOINI AND UN_PROYECTOFIN
        AND   COMPONENTES.VALORTOTAL     < PROG.VALORPROGRAMADOCOM
      GROUP BY COMPONENTES.VIGENCIA,
              COMPONENTES.COMPANIA,
              COMPONENTES.CODIGOPROYECTO,
              COMPONENTES.TIPOCOMPONENTE,
              COMPONENTES.CODIGO,
              COMPONENTES.VALORTOTAL
    )
    LOOP
      MI_STRLOGC :=  MI_STRLOGC || CHR(10)||'Proyecto:         ' || RS_VALCOMP.CODIGOPROY     || CHR(10) ||
                                            'Componente:       ' || RS_VALCOMP.CODCOMPONENTE  || CHR(10) ||
                                            'Valor total:      ' || RS_VALCOMP.VALORTOTAL     || CHR(10) ||
                                            'Valor programado: ' || RS_VALCOMP.VALORPROGRAMADOCOM || CHR(13) || CHR(10);
      IF RS_VALCOMP.CODIGOPROY IS NOT NULL THEN
        MI_CONTADORC := MI_CONTADORC +1;
      END IF;
    END LOOP COMPONENTES;

    IF MI_CONTADORC = 0 THEN
      MI_STRLOGC   := NULL;
      MI_CONTADORC := NULL;
    END IF;

    -- OBTENER LOS DATOS QUE LA VARIABLE MI_STRLOGCA RETORNARÃ¿ PARA INFORMAR LAS INCONSISTENCIAS OCURRIDAS DURANTE EL PROCESO DE ACTUALIZACION
    -- DE LA TABLA PROYECTOS
    <<PROYECTOS>>
    FOR RS_VALPROY IN (
      SELECT  PROYECTOS.VIGENCIAINICIO,
              PROYECTOS.COMPANIA,
              PROYECTOS.CODIGO CODIGOPROY,
              PROYECTOS.VALORTOTAL,
              SUM(NVL(PROG.VALORPROGRAMADOPRO,0)) VALORPROGRAMADOPRO
      FROM    PROYECTOS
        LEFT JOIN (SELECT PROGRAMACION.COMPANIA,
                          PROGRAMACION.CODIGOPROYECTO,
                          PROGRAMACION.TIPOCOMPONENTE,
                          PROGRAMACION.CODIGOCOMPONENTE,
                          PROGRAMACION.CODIGOACTIVIDAD,
                          (NVL(VALOR1,0) + NVL(VALOR2,0) + NVL(VALOR3,0) + NVL(VALOR4,0)
                            + NVL(VALOR5,0) + NVL(VALOR6,0) + NVL(VALOR7,0) + NVL(VALOR8,0)
                            + NVL(VALOR9,0) + NVL(VALOR10,0) + NVL(VALOR11,0) + NVL(VALOR12,0)) VALORPROGRAMADOPRO
                  FROM    PROGRAMACION
                    INNER JOIN PROYECTOS
                      ON  PROGRAMACION.COMPANIA       = PROYECTOS.COMPANIA
                      AND PROYECTOS.VIGENCIAINICIO    = UN_VIGENCIA
                      AND PROGRAMACION.CODIGOPROYECTO = PROYECTOS.CODIGO
                  WHERE   PROGRAMACION.COMPANIA = UN_COMPANIA
                    AND   PROGRAMACION.TIPOESTADO IN ('P', 'RP')) PROG
          ON  PROYECTOS.COMPANIA = PROG.COMPANIA
          AND PROYECTOS.CODIGO = PROG.CODIGOPROYECTO
      WHERE   PROYECTOS.COMPANIA = UN_COMPANIA
        AND   PROYECTOS.CODIGO BETWEEN UN_PROYECTOINI AND UN_PROYECTOFIN
        AND   PROYECTOS.VALORTOTAL < PROG.VALORPROGRAMADOPRO
      GROUP BY PROYECTOS.VIGENCIAINICIO,
              PROYECTOS.COMPANIA,
              PROYECTOS.CODIGO,
              PROYECTOS.VALORTOTAL
    )
    LOOP
      MI_STRLOGP :=  MI_STRLOGP || CHR(10)|| 'Proyecto:         ' || RS_VALPROY.CODIGOPROY || CHR(10) ||
                                             'Valor total:      ' || RS_VALPROY.VALORTOTAL || CHR(10) ||
                                             'Valor programado: ' || RS_VALPROY.VALORPROGRAMADOPRO || CHR(10) || CHR(13);
      IF RS_VALPROY.CODIGOPROY IS NOT NULL THEN
        MI_CONTADORP := MI_CONTADORP +1;
      END IF;
    END LOOP PROYECTOS;

    IF MI_CONTADORP = 0 THEN
      MI_STRLOGP   := NULL;
      MI_CONTADORP := NULL;
    END IF;

    IF MI_STRLOGCA IS NOT NULL AND MI_CONTADORCA IS NOT NULL THEN
      MI_STRLOGCA := 'Por favor verifique las siguientes actividades: ' || CHR(10) || MI_STRLOGCA;
    END IF;

    IF MI_STRLOGC IS NOT NULL AND MI_CONTADORC IS NOT NULL THEN
      MI_STRLOGC  := 'Por favor verifique los siguientes componentes: ' || CHR(10) || MI_STRLOGC;
    END IF;

    IF MI_STRLOGP IS NOT NULL AND MI_CONTADORP IS NOT NULL THEN
      MI_STRLOGP  := 'Por favor verifique los siguientes proyectos: ' || CHR(10)  || MI_STRLOGP;
    END IF;

    IF (MI_STRLOGCA || MI_STRLOGC || MI_STRLOGP) IS NULL THEN
      MI_RTA := NULL;
    ELSE
      MI_RTA := MI_STRLOGCA || '<error>' || MI_STRLOGC || '<error>' || MI_STRLOGP || '<error>' || MI_CONTADORCA || '<error>' || MI_CONTADORC || '<error>' || MI_CONTADORP;
    END IF;

    RETURN MI_RTA;
  END FC_ACTU_VALORPROGRAMADO;

