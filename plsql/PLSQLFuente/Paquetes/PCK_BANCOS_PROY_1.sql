      GROUP BY COMPONENTES_ACTIVIDADES.VIGENCIA,
              COMPONENTES_ACTIVIDADES.COMPANIA,
              COMPONENTES_ACTIVIDADES.CODIGOPROYECTO,
              COMPONENTES_ACTIVIDADES.TIPOCOMPONENTE,
              COMPONENTES_ACTIVIDADES.COMPONENTE,
