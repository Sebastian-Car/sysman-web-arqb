      AUTHOR MIGRACION  : LEYDI MILENA CORTES FORERO
      DATE MIGRADOR     : 06/10/2015
      TIME              : 16:41 PM
      SOURCE MODULE     : BancoProyectos
      MODIFIER          : YEISSON ALEJANDRO ROJAS RUIZ
      DATE MODIFIED     : 31/01/2017
      TIME              : 03:00 PM
      DESCRIPTION       : PERMITE ACTUALIZAR EL ESTADO DE LAS NOVEDADES DE PROYECTO HASTA UNA FECHA DEPENDIENDO DEL TIPO DE NOVEDAD. HACE USO DE LA
                          FUNCION FC_ANULAR_SOLICITUD Y RETORNA UN RESUMEN DE ANULACIÃ?N DE LAS NOVEDADES ANULADAS Y NO ANULADAS.
      PARAMETERS        : UN_FECHA    => FECHA POR LA CUAL SE FILTRA.
