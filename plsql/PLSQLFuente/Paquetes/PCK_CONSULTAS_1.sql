  FUNCTION FC_RESUELVECONSULTA
  /*
    NAME              : FC_RESUELVECONSULTA 
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : JESSICA LISSETH RAMIREZ BRICEÑO
    DATE MIGRADOR     : 13/03/2017
    TIME              : 03:56 PM
    MODIFIER          : 
    DATE MODIFIED     : 
    TIME              : 
    DESCRIPTION       : FUNCION QUE REEMPLAZA VARIABLES EN UNA CONSULTA DADA.
    PARAMETERS        : UN_CONSULTA   => CONSULTA QUE VA A SER MODIFICADA.
                        UN_REEMPLAZOS => ARREGLO DE VARIABLES QUE SE QUIEREN MODIFICAR
                                         DENTRO DE LA CONSULTA, CON SU RESPECTIVO VALOR. 
    MODIFICATIONS     : 
    @NAME:  resolverConsulta
    @METHOD:  GET
    */ 
  (
    UN_CONSULTA   IN CLOB
   ,UN_REEMPLAZOS IN PCK_SUBTIPOS.TI_CLAVEVALOR 
  )
    RETURN CLOB
  AS
    MI_CONSULTA CLOB;
  BEGIN
    MI_CONSULTA := UN_CONSULTA;
    FOR MI_I IN 1..UN_REEMPLAZOS.COUNT LOOP
      MI_CONSULTA := REPLACE(MI_CONSULTA,'s$'||UN_REEMPLAZOS(MI_I).CLAVE||'$s',UN_REEMPLAZOS(MI_I).VALOR);
    END LOOP;
    RETURN MI_CONSULTA;
  END FC_RESUELVECONSULTA;

END PCK_CONSULTAS;