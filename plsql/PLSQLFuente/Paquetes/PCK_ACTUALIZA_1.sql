PROCEDURE PR_CREAR_CAMPO_CA
/*
    NAME              : PR_CREAR_CAMPO_CA --> EN ACCESS CrearCampoCA
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : NICOLÁS GÓMEZ BARBOSA
    DATE MIGRADOR     : 21/01/2016
    TIME              : 11:00 AM
    SOURCE MODULE     : SysmanAl2015.10.01 Almacén
    DESCRIPTION       : Crea un campo en la base de datos actual mediante los parámetros Nombre tabla, campo , tipo-tamaño y parámetros default y nulo.
    @NAME: crearCampo
  */
  (
    UN_TABLA             IN VARCHAR2,
    UN_CAMPO             IN VARCHAR2,
    UN_TIPO_TAMANO       IN VARCHAR2,
    UN_PARAMETROS_NULL   IN VARCHAR2:='',
    UN_PARAMETROS_DEFAULT IN VARCHAR2:=''
  )
  AS
    MI_ERROR_FUN        NUMBER:= GL_ERROR_NUM + 2;
    MI_SQL              VARCHAR2(32000);
  BEGIN

    IF PCK_SYSMAN_UTL.FC_EXISTETABLA(UPPER(UN_TABLA)) NOT IN(0) AND PCK_SYSMAN_UTL.FC_EXISTECAMPO(UPPER(UN_TABLA),UPPER(UN_CAMPO)) = FALSE THEN

      MI_SQL:='ALTER TABLE "'||UPPER(UN_TABLA)||'" ADD "'||UPPER(UN_CAMPO)||'" '||UPPER(UN_TIPO_TAMANO);
      EXECUTE IMMEDIATE MI_SQL; 

      IF UN_PARAMETROS_DEFAULT IS NOT NULL AND UPPER(UN_PARAMETROS_DEFAULT)<>'NULL' THEN
        PCK_ACTUALIZA.PR_CAMPO_DEFAULT(UPPER(UN_TABLA),UPPER(UN_CAMPO),UN_PARAMETROS_DEFAULT);
      END IF;

      IF UPPER(UN_PARAMETROS_NULL)='NOT NULL' THEN
        PCK_ACTUALIZA.PR_CAMBIAR_NULL(UPPER(UN_TABLA),UPPER(UN_CAMPO),UPPER(UN_PARAMETROS_NULL));
      END IF;     

    END IF;    

  EXCEPTION WHEN OTHERS THEN
    PCK_DATOS.GL_ERROR_MSG := 'Error al crear campo';
    PCK_DATOS.GL_ERROR_MSG := PCK_SYSMAN_UTL.FC_EVALUAR_ERROR(MI_ERROR_FUN, PCK_DATOS.GL_ERROR_MSG,  UN_TABLA,'',SQLERRM );
    RAISE_APPLICATION_ERROR (-20000, PCK_DATOS.GL_ERROR_MSG || CHR(10) || PCK_DATOS.GL_ERROR_MSG ); 

END PR_CREAR_CAMPO_CA;

