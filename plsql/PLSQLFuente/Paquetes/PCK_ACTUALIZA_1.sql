PROCEDURE PR_CREAR_TABLA_CA
/*
    NAME              : PR_CREAR_TABLA_CA --> EN ACCESS CrearTablaCA
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : NICOLÁS GÓMEZ BARBOSA
    DATE MIGRADOR     : 21/01/2016
    TIME              : 10:00 AM
    SOURCE MODULE     : SysmanAl2015.10.01 Almacén
    DESCRIPTION       : Crea una tabla en la base de datos actual mediante los parámetros Nombre tabla, campos y Llaves primarias
    @NAME: crearTabla               
  */
  (
    UN_TABLA        IN    VARCHAR2,
    UN_CAMPOS       IN    VARCHAR2,
    UN_LLAVES       IN    VARCHAR2,
    UN_TABLESPACE   IN    VARCHAR2
  )
  AS
    MI_ERROR_FUN        NUMBER:= GL_ERROR_NUM + 1;
    MI_SQL              VARCHAR2(32000);
  BEGIN    

    IF PCK_SYSMAN_UTL.FC_EXISTETABLA(UPPER(UN_TABLA))IN(0) THEN
      MI_SQL:='CREATE TABLE "'||UPPER(UN_TABLA)||'" ('||UPPER(UN_CAMPOS)||', CONSTRAINT "PK_'||UPPER(UN_TABLA)||'" PRIMARY KEY('||UPPER(UN_LLAVES)||')) TABLESPACE '||UPPER(UN_TABLESPACE);
      EXECUTE IMMEDIATE MI_SQL; 
    END IF;

    EXCEPTION WHEN OTHERS THEN
      PCK_DATOS.GL_ERROR_MSG := 'Error al crear tabla';
      PCK_DATOS.GL_ERROR_MSG := PCK_SYSMAN_UTL.FC_EVALUAR_ERROR(MI_ERROR_FUN, PCK_DATOS.GL_ERROR_MSG,  UN_TABLA,'',SQLERRM );
      RAISE_APPLICATION_ERROR (-20000, PCK_DATOS.GL_ERROR_MSG || CHR(10) || PCK_DATOS.GL_ERROR_MSG ); 


END PR_CREAR_TABLA_CA;


