PROCEDURE PR_CAMBIAR_NULL
/*
    NAME              : PR_CAMBIAR_NULL
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : NICOLÁS GÓMEZ BARBOSA
    DATE MIGRADOR     : 22/01/2016
    TIME              : 11:00 AM
    DESCRIPTION       : Cambia un campo de null a not null o viceversa.
    @NAME: cambiarValorporDefecto
  */
  (
    UN_TABLA          IN    VARCHAR2,
    UN_CAMPO          IN    VARCHAR2,
    UN_NUEVOESTADO    IN    VARCHAR2
  )
  AS
    MI_ERROR_FUN    NUMBER:= GL_ERROR_NUM + 4;
    MI_SQL          VARCHAR2(32000);
    MI_CAMPOS       VARCHAR2(32000);
    MI_CONDICION    VARCHAR2(32000);
    MI_ESTADO       VARCHAR2(32000);
    MI_DEFAULT      VARCHAR2(32000);
    MI_I            NUMBER;
  BEGIN

  IF PCK_SYSMAN_UTL.FC_EXISTETABLA(UPPER(UN_TABLA))NOT IN(0) AND PCK_SYSMAN_UTL.FC_EXISTECAMPO(UPPER(UN_TABLA),UPPER(UN_CAMPO))=TRUE AND (UPPER(UN_NUEVOESTADO)='NULL' OR UPPER(UN_NUEVOESTADO)='NOT NULL') THEN

    MI_SQL:='SELECT  CASE WHEN NULLABLE=''Y'' THEN ''NULL'' ELSE ''NOT NULL'' END FROM USER_TAB_COLUMNS WHERE TABLE_NAME='''||UPPER(UN_TABLA)||''' AND COLUMN_NAME='''||UN_CAMPO||'''';
    EXECUTE IMMEDIATE MI_SQL INTO MI_ESTADO;
    MI_SQL:='SELECT  DATA_DEFAULT FROM USER_TAB_COLUMNS WHERE TABLE_NAME='''||UPPER(UN_TABLA)||''' AND COLUMN_NAME='''||UN_CAMPO||'''';
    EXECUTE IMMEDIATE MI_SQL INTO MI_DEFAULT;
    MI_SQL:='SELECT  COUNT(*) FROM '||UPPER(UN_TABLA);
    EXECUTE IMMEDIATE MI_SQL INTO MI_I;

    IF MI_ESTADO<>UPPER(UN_NUEVOESTADO) THEN

      IF MI_ESTADO='NULL' THEN
        IF MI_I=0 THEN
          MI_SQL:='ALTER TABLE '||UPPER(UN_TABLA)||' MODIFY ('||UN_CAMPO||' NOT NULL)'; 
        ELSIF MI_DEFAULT IS NOT NULL THEN      

          MI_CAMPOS:=UN_CAMPO||'='||MI_DEFAULT;
          MI_CONDICION:=UN_CAMPO||' IS NULL';
          PCK_DATOS.GL_RTA:= PCK_DATOS.FC_ACME(UN_TABLA,'M',MI_CAMPOS,NULL,NULL,MI_CONDICION,NULL,NULL,NULL,NULL);

          MI_SQL:='ALTER TABLE '||UPPER(UN_TABLA)||' MODIFY ('||UN_CAMPO||' NOT NULL)';
        END IF;  
      ELSE
        MI_SQL:='ALTER TABLE '||UPPER(UN_TABLA)||' MODIFY ('||UN_CAMPO||' NULL)';
      END IF; 

      EXECUTE IMMEDIATE MI_SQL; 

    END IF;

  END IF;

  EXCEPTION WHEN OTHERS THEN
    PCK_DATOS.GL_ERROR_MSG := 'Error al cambiar null a campo';
    PCK_DATOS.GL_ERROR_MSG := PCK_SYSMAN_UTL.FC_EVALUAR_ERROR(MI_ERROR_FUN, PCK_DATOS.GL_ERROR_MSG,  UN_TABLA,'',SQLERRM );
    RAISE_APPLICATION_ERROR (-20000, PCK_DATOS.GL_ERROR_MSG || CHR(10) || PCK_DATOS.GL_ERROR_MSG );

END PR_CAMBIAR_NULL;



END PCK_ACTUALIZA;