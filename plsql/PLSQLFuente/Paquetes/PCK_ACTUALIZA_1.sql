PROCEDURE PR_CAMPO_DEFAULT
/*
    NAME              : PR_CAMPO_DEFAULT --> EN ACCESS CampoDefault
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : NICOLÁS GÓMEZ BARBOSA
    DATE MIGRADOR     : 22/01/2016
    TIME              : 09:00 AM
    SOURCE MODULE     : SysmanBP2015.07.01 Banco de Proyectos.
    DESCRIPTION       : Asigna un Default a un campo.
    @NAME: crearValorDefecto
  */
  (
    UN_TABLA          IN    VARCHAR2,
    UN_CAMPO          IN    VARCHAR2,
    UN_VAL_DEFAULT    IN    VARCHAR2  
  )
  AS
    MI_ERROR_FUN    NUMBER:= GL_ERROR_NUM + 3;
    MI_SQL          VARCHAR2(32000);
    MI_CAMPOS       VARCHAR2(32000);
    MI_CONDICION    VARCHAR2(32000);
  BEGIN

  IF PCK_SYSMAN_UTL.FC_EXISTETABLA(UPPER(UN_TABLA))NOT IN(0) AND PCK_SYSMAN_UTL.FC_EXISTECAMPO(UPPER(UN_TABLA),UPPER(UN_CAMPO))= TRUE THEN

    MI_SQL:='ALTER TABLE '||UPPER(UN_TABLA)||' MODIFY ('||UN_CAMPO||' DEFAULT '||UN_VAL_DEFAULT||')';
    EXECUTE IMMEDIATE MI_SQL;

    MI_CAMPOS:=UN_CAMPO||'='||UN_VAL_DEFAULT;
    MI_CONDICION:=UN_CAMPO||' IS NULL';
    PCK_DATOS.GL_RTA:= PCK_DATOS.FC_ACME(UN_TABLA,'M',MI_CAMPOS,NULL,NULL,MI_CONDICION,NULL,NULL,NULL,NULL);    

  END IF;

  EXCEPTION WHEN OTHERS THEN
    PCK_DATOS.GL_ERROR_MSG := 'Error al asignar default a campo';
    PCK_DATOS.GL_ERROR_MSG := PCK_SYSMAN_UTL.FC_EVALUAR_ERROR(MI_ERROR_FUN, PCK_DATOS.GL_ERROR_MSG,  UN_TABLA,'',SQLERRM );
    RAISE_APPLICATION_ERROR (-20000, PCK_DATOS.GL_ERROR_MSG || CHR(10) || PCK_DATOS.GL_ERROR_MSG );

END PR_CAMPO_DEFAULT;

