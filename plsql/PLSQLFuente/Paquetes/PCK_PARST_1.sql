PROCEDURE PR_INICIALIZAR_PARSISTEMA
     /*
    NAME              : PR_INICIALIZAR_PARSISTEMA
    AUTHORS           : SYSMAN LTDA
    AUTHOR MIGRACION  : SANDRA MILENA DAZA
    DATE MIGRADOR     : 01/06/2015
    TIME              :
    MODIFIER          : JOSE PASCUAL GOMEZ / MIGUEL ZANGUÑA
    DATE MODIFIED     : 22/07/2015 / 23/02/2017
    TIME              : 2:00 PM
    DESCRIPTION       : Carga en un type todos los parametros de la base de datos
    MODIFICATIONS     : Inlucir el filtro de fecha y modulo
  */
  (
    UN_COMPANIA VARCHAR2,
    UN_MODULO NUMBER,
    UN_FECHA_PAR DATE:= SYSDATE
  )
AS
BEGIN
/*
  FOR RS IN (SELECT *
             FROM   PARAMETRO
             WHERE  COMPANIA = UN_COMPANIA
            )LOOP
    LSTPAR(RS.NOMBRE) := RS.VALOR;
  END LOOP;
*/
FOR RS IN(
    SELECT PARAMETRO.NOMBRE, PARAMETRO.VALOR
    FROM   PARAMETRO INNER JOIN
    (
      SELECT PARAMETRO.COMPANIA, PARAMETRO.NOMBRE, PARAMETRO.MODULO,  MAX(PARAMETRO.FECHA_INICIAL) FECHA_INICIAL
      FROM   PARAMETRO
      WHERE  PARAMETRO.COMPANIA = UN_COMPANIA
         AND PARAMETRO.MODULO      IN (UN_MODULO)
         AND PARAMETRO.FECHA_INICIAL < = UN_FECHA_PAR
      GROUP BY PARAMETRO.COMPANIA, PARAMETRO.NOMBRE, PARAMETRO.MODULO

      UNION

      SELECT PARAMETRO.COMPANIA, PARAMETRO.NOMBRE, PARAMETRO.MODULO, MAX(PARAMETRO.FECHA_INICIAL) FECHA_INICIAL
      FROM   PARAMETRO
      WHERE  PARAMETRO.COMPANIA = UN_COMPANIA
         AND PARAMETRO.MODULO      IN(-1)
         AND PARAMETRO.FECHA_INICIAL < = UN_FECHA_PAR
         AND PARAMETRO.NOMBRE NOT IN
             (SELECT PARAMETRO.NOMBRE
              FROM   PARAMETRO
              WHERE  PARAMETRO.COMPANIA = UN_COMPANIA
                AND PARAMETRO.MODULO      IN(UN_MODULO)
                AND PARAMETRO.FECHA_INICIAL < = UN_FECHA_PAR
              GROUP BY PARAMETRO.COMPANIA, PARAMETRO.NOMBRE, PARAMETRO.MODULO)
      GROUP BY PARAMETRO.COMPANIA, PARAMETRO.NOMBRE, PARAMETRO.MODULO
    ) UNICO
    ON PARAMETRO.COMPANIA       = UNICO.COMPANIA
    AND PARAMETRO.NOMBRE        = UNICO.NOMBRE
    AND PARAMETRO.FECHA_INICIAL = UNICO.FECHA_INICIAL
    AND PARAMETRO.MODULO        = UNICO.MODULO
  ) LOOP
    LSTPAR(RS.NOMBRE) := RS.VALOR;
  END LOOP;


END PR_INICIALIZAR_PARSISTEMA;

FUNCTION FC_PAR
 /*
    NAME              : PAR
    AUTHORS           : SYSMAN SAS
    AUTHOR MIGRACION  : SANDRA MILENA DAZA LEGUIZAMON
    DATE MIGRADOR     : 06/03/2015 
    TIME              : 2:30 PM     
    MODIFIER          : 
    DATE MODIFIED     : 
    DESCRIPTION       : RETORNA EL VALOR DE LA POSICI¿ DEL ARREGLO DE PARAMETROS SEGUN EL INDICE UNICO DEL ARREGLO 
                        Y VALIDANDO CUANDO ESTE NO EXISTE ASIGNANDO EL VALOR POR OMISION QUE INGRESA POR PARAMETRO
    MODIFICATIONS     : 
  */
  (
    UN_PARAMETRO VARCHAR2,
    UN_VLOMISION VARCHAR2
  )
RETURN VARCHAR2
AS
  MI_VLRPARAMETRO VARCHAR2(500);
BEGIN
  MI_VLRPARAMETRO := LSTPAR(UN_PARAMETRO);
  IF MI_VLRPARAMETRO IS NULL  THEN
    RETURN UN_VLOMISION;
  END IF;
  RETURN MI_VLRPARAMETRO;
  EXCEPTION WHEN NO_DATA_FOUND THEN
    RETURN UN_VLOMISION;
END FC_PAR;

