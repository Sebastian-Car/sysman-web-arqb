    IF UN_ARRANCA =NULL THEN
      MI_INICIO :=1;
    ELSIF UN_ARRANCA < 0 OR UN_ARRANCA>7 THEN
      RETURN NULL;
    ELSE
      MI_INICIO :=UN_ARRANCA;
    END IF;
    --Por cuestiones de territorio (NLS_TERRITORY) el inicio de semana se puede dar el domingo o el lunes
