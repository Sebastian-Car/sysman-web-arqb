    FROM DUAL;    

  --Por defecto se inicializa la semana en domingo y en base a eso se devuelve
    IF MI_INICIA=1 THEN
      MI_NUMDIA :=MI_NUMDIA+1;
    IF MI_NUMDIA=8 THEN
      MI_NUMDIA:=1;
    END IF;
  END IF;

