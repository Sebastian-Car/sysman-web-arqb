  IF MI_NUMDIA<=0 THEN
    MI_NUMDIA:=7+MI_NUMDIA;
  END IF;  
  RETURN (MI_NUMDIA);   
  EXCEPTION WHEN OTHERS THEN
  RETURN (1);
