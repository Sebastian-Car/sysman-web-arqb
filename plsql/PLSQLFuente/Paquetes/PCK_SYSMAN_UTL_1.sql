                    MI_I := MI_I + 1;
                END;
            END IF;
            IF MI_I < UN_DIAS THEN
                MI_FECHAHABIL := MI_FECHAHABIL +1;
            END IF;
        END LOOP;
    ELSE
        WHILE MI_I <= UN_DIAS LOOP
            MI_NRODIA := PCK_SYSMAN_UTL.FC_WEEKDAY(MI_FECHAHABIL);
            IF MI_NRODIA <> 1 THEN
