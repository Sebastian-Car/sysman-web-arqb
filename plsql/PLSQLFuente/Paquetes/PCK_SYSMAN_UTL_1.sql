    @NAME:  retornarFechaMasDiasHabiles
    @METHOD:  GET
  */
  (
    UN_COMPANIA IN PCK_SUBTIPOS.TI_COMPANIA,
    UN_FECHA    IN DATE,
    UN_DIAS     IN PCK_SUBTIPOS.TI_ENTERO,
    UN_SABADO   IN PCK_SUBTIPOS.TI_ENTERO DEFAULT 0
  )
