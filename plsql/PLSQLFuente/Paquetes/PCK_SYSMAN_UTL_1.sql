     MI_VLRETORNO := MI_VLRETORNO - 1;
    END IF;
  END LOOP DIADELASEMANA;
  RETURN MI_VLRETORNO;

  EXCEPTION WHEN OTHERS THEN
    PCK_DATOS.GL_ERROR_MSG := 'Interrupción durante el cálculo de días hábiles entre las fechas  ' || UN_FECHAINI || ' y ' || UN_FECHAFIN ;
