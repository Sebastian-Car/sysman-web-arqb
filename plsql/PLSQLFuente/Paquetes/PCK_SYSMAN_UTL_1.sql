    @NAME:  delimitarCadena
    @METHOD:  GET                 
  */ 
  UN_LISTA       IN CLOB,
  UN_DELIMITADOR IN VARCHAR2 :=','
  )
  RETURN T_SPLITCL 
  AS
  MI_I     INTEGER:=0;
  MI_POS   INTEGER:=0; 
  MI_LISTA CLOB:=UN_LISTA;
  MI_VALOR T_SPLITCL;
