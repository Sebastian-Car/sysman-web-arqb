      MI_AI:=MI_AI+1;
      MI_MI:=MI_MI-12; 
    END IF;  

    MI_FECHAINICIAL:=TO_DATE(''||TO_CHAR(MI_DI)||'/'||TO_CHAR(MI_MI)||'/'||TO_CHAR(MI_AI)||'','DD/MM/YYYY');
    MI_FECHAFIN:=MI_FECHAINICIAL;
  ELSE
    MI_FECHAFIN:=UN_FECHAINICIAL+MI_DIAS;
