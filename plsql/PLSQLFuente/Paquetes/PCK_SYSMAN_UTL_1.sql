AS
    MI_NRODIA     PCK_SUBTIPOS.TI_ENTERO;
    MI_FECHAHABIL DATE;
    MI_CONTADOR   NUMBER;
    MI_I          PCK_SUBTIPOS.TI_ENTERO:= 0;
    MI_EXISTE     VARCHAR2(1 CHAR);
