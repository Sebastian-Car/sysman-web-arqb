                    SELECT  'X'
                    INTO    MI_EXISTE
                    FROM    FESTIVOS
                    WHERE   COMPANIA = UN_COMPANIA
                      AND   TO_CHAR(ID_DE_FESTIVO,'YYYYMMDD') = TO_CHAR(MI_FECHAHABIL, 'YYYYMMDD');
