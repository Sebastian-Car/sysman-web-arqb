								AND DETALLE_COMPROBANTE_PPTAL.ANO_AFECT       	= RS_REGISTROS_OBLIGACIONES.ANO
								AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT 	= RS_REGISTROS_OBLIGACIONES.TIPO_CPTE
