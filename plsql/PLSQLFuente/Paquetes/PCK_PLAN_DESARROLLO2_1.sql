							AND  PI_PLAN_INDICATIVO_FUENTES.NUMERO         	=  '||UN_NUMERO||'
							AND  LENGTH(PI_PLAN_INDICATIVO_FUENTES.ID_PLAN) =  '||UN_DIGITOSACCION||'';
