			EXECUTE IMMEDIATE MI_STRSQL INTO MI_EXISTE;
			EXCEPTION WHEN NO_DATA_FOUND THEN
				RETURN 0;
		END;

		MI_STRSQL := '	SELECT DISTINCT ''X'' EXISTE            
						FROM   PI_PLAN_ADQUISICIONES                     
