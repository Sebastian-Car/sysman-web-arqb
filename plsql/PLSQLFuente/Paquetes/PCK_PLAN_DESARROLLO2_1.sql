PROCEDURE PR_SALDODISPONIBLE 
(
  /*
  NAME              : PR_SALDODISPONIBLE nombre en access tieneSaldoDisponible()
  AUTHORS           : SYSMAN  SAS
  AUTHOR MIGRACION  : YESIKA PAOLA BECERRA CASTRO
  DATE MIGRADOR     : 16/07/2018
  TIME              : 11:50 AM
  SOURCE MODULE     : Plan Desarrollo
  MODIFIER          : 
  DATE MODIFIED     : 
  TIME              : 
  DESCRIPTION       : Valida si se puede ingresar el valor prepuestado para la fuente de recurso seleccionda
  MODIFIER          : 
  DATE MODIFIED     : 
  DESCRIPTION       : 
  @NAME:  verificarSaldoDisponible
  @METHOD:  GET
*/
	UN_COMPANIA 		      IN PCK_SUBTIPOS.TI_COMPANIA,
  UN_ID     			      IN VARCHAR2,
	UN_ID_PLAN			      IN VARCHAR2,
	UN_VIGENCIA_PLAN 	    IN PCK_SUBTIPOS.TI_ANIO,
	UN_VIGENCIA_META 	    IN PCK_SUBTIPOS.TI_ANIO,
	UN_VIGENCIA_INICIAL	  IN PCK_SUBTIPOS.TI_ANIO,
	UN_TIPO 			        IN VARCHAR2,
	UN_NUMERO 			      IN VARCHAR2,
	UN_FUENTE 			      IN PCK_SUBTIPOS.TI_FUENTE_RECURSOS,
	UN_VALORFUENTE		    IN PCK_SUBTIPOS.TI_DOBLE
)
AS 
	MI_STRSQL 				      PCK_SUBTIPOS.TI_STRSQL;
	MI_SALDO 				        PCK_SUBTIPOS.TI_DOBLE;
  MI_SALDOACTUAL	        PCK_SUBTIPOS.TI_DOBLE;
	MI_SALDOTOTAL 			    PCK_SUBTIPOS.TI_DOBLE;
	MI_ACCION 				      PCK_SUBTIPOS.TI_ENTERO_LARGO;
	MI_SALDODISPONIBLE 		  PCK_SUBTIPOS.TI_DOBLE;
	MI_REEMPLAZOS       	  PCK_SUBTIPOS.TI_CLAVEVALOR;
	MI_MSJSALDO				      VARCHAR2(25 CHAR);
	MI_MSJSALDODISPONIBLE 	VARCHAR2(25 CHAR);
	MI_MSJVALORFUENTES 		  VARCHAR2(25 CHAR);
  MI_NIVEL                PCK_SUBTIPOS.TI_ENTERO_LARGO;
BEGIN 
		MI_STRSQL := '	SELECT NVL(SUM(VALOR_PRESUPUESTO_FIN),0) AS SALDO       
                    FROM   PI_PLAN_INDICATIVO_FUENTES 
                    WHERE  COMPANIA      	= '''||UN_COMPANIA||'''
                      AND  ID_PLAN       	= '''||UN_ID||'''
                      AND  VIGENCIA_PLAN 	= 	'||UN_VIGENCIA_PLAN||'
                      AND  VIGENCIA_META 	=  	'||UN_VIGENCIA_META||'
                      AND  TIPO          	= '''||UN_TIPO||'''
                      AND  NUMERO        	=  	'||UN_NUMERO||'
                      AND  FUENTE        	= '''||UN_FUENTE||'''';
		BEGIN  
			EXECUTE IMMEDIATE MI_STRSQL INTO MI_SALDO; 
			EXCEPTION WHEN NO_DATA_FOUND THEN 
			MI_SALDO := 0; 
		END; 			

		IF MI_SALDO <> 0 THEN 
			MI_NIVEL := PCK_PLAN_DESARROLLO.FC_CARGAR_NIVEL(  UN_COMPANIA => UN_COMPANIA,
                                                        UN_VIGENCIA => UN_VIGENCIA_PLAN);
			MI_ACCION := PCK_PLAN_DESARROLLO.FC_GET_ACCION();									
			MI_STRSQL := '	SELECT NVL(SUM(PI_PLAN_INDICATIVO_FUENTES.VALOR_PRESUPUESTO_FIN),0) AS PRESUPUESTO_ACTUAL   
                      FROM   PI_PLAN_INDICATIVO_FUENTES   
                        INNER  JOIN PI_PLAN_INDICATIVO                                                      
                          ON  PI_PLAN_INDICATIVO_FUENTES.COMPANIA       = PI_PLAN_INDICATIVO.COMPANIA 
                          AND PI_PLAN_INDICATIVO_FUENTES.VIGENCIA_PLAN  = PI_PLAN_INDICATIVO.VIGENCIA_INICIAL
                          AND  PI_PLAN_INDICATIVO_FUENTES.TIPO          = PI_PLAN_INDICATIVO.TIPO
                          AND  PI_PLAN_INDICATIVO_FUENTES.NUMERO        = PI_PLAN_INDICATIVO.NUMERO
                          AND  PI_PLAN_INDICATIVO_FUENTES.ID_PLAN       = PI_PLAN_INDICATIVO.ID_PLAN
                      WHERE  PI_PLAN_INDICATIVO_FUENTES.COMPANIA       = '''||UN_COMPANIA||'''
                        AND  PI_PLAN_INDICATIVO_FUENTES.VIGENCIA_PLAN  =  '||UN_VIGENCIA_INICIAL||'
                        AND  PI_PLAN_INDICATIVO_FUENTES.VIGENCIA_META  =  '||UN_VIGENCIA_META||'
                        AND  PI_PLAN_INDICATIVO_FUENTES.TIPO           = '''||UN_TIPO||'''
                        AND  PI_PLAN_INDICATIVO_FUENTES.NUMERO         = '||UN_NUMERO||'
                        AND  LENGTH(PI_PLAN_INDICATIVO.ID_PLAN)        = '||MI_ACCION||'
                        AND  PI_PLAN_INDICATIVO_FUENTES.FUENTE         = '''||UN_FUENTE||'''
                        AND  PI_PLAN_INDICATIVO_FUENTES.ID_PLAN NOT IN('''||UN_ID_PLAN||''')
                        AND  PI_PLAN_INDICATIVO_FUENTES.ID_PLAN IN                                          
                        (                                                                                 
                          SELECT ID_PLAN                                                                  
                          FROM   PI_PLAN_INDICATIVO                                                       
                          WHERE  COMPANIA             = '''||UN_COMPANIA||'''                                         
                            AND  VIGENCIA_INICIAL     =  '||UN_VIGENCIA_INICIAL||'
                            AND  TIPO                 = '''||UN_TIPO||'''
                            AND  NUMERO               =  '||UN_NUMERO||'
                            AND  PREDECESOR           = '''||UN_ID||'''  
                            AND  (LENGTH(ID_PLAN)     =  '||MI_ACCION||'
                                  OR   ESNUEVO NOT IN (0))                                                                      
                        )  ';
		BEGIN  
			EXECUTE IMMEDIATE MI_STRSQL INTO MI_SALDOACTUAL; 
			EXCEPTION WHEN NO_DATA_FOUND THEN 
			MI_SALDOACTUAL := 0; 
		END; 
  		MI_SALDOTOTAL := MI_SALDOACTUAL + UN_VALORFUENTE;
			BEGIN
				IF  MI_SALDOTOTAL > MI_SALDO THEN 
					IF MI_SALDO - MI_SALDOACTUAL > 0 THEN 
						MI_SALDODISPONIBLE := MI_SALDO - MI_SALDOACTUAL;
					ELSE 
						MI_SALDODISPONIBLE := 0;
					END IF;
        MI_MSJSALDO				:= TO_CHAR(MI_SALDO,'FM999,999,999');
				MI_MSJSALDODISPONIBLE 	:= TO_CHAR(MI_SALDODISPONIBLE,'FM999,999,999');
				MI_MSJVALORFUENTES 		:= TO_CHAR(UN_VALORFUENTE,'FM999,999,999');  
        RAISE PCK_EXCEPCIONES.EXC_PLANDES;
				END IF;


			EXCEPTION WHEN PCK_EXCEPCIONES.EXC_PLANDES THEN
				MI_REEMPLAZOS(0).CLAVE := 'SALDO';
				MI_REEMPLAZOS(0).VALOR := MI_MSJSALDO;
				MI_REEMPLAZOS(1).CLAVE := 'DISPONIBLE';
				MI_REEMPLAZOS(1).VALOR := MI_MSJSALDODISPONIBLE;
				MI_REEMPLAZOS(2).CLAVE := 'VALORFUENTE';
				MI_REEMPLAZOS(2).VALOR := MI_MSJVALORFUENTES;
				PCK_ERR_MSG.RAISE_WITH_MSG(	UN_EXC_COD    	=> SQLCODE,
                                    UN_ERROR_COD  	=> PCK_ERRORES.ERR_SALDOFUENTES,
                                    UN_REEMPLAZOS 	=> MI_REEMPLAZOS);
			END;			

		END IF;
END PR_SALDODISPONIBLE;	

