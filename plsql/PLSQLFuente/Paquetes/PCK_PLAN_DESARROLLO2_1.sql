  MODIFIER          : 
  DATE MODIFIED     : 
  DESCRIPTION       : 
  @NAME:  verificarPlanAdquisiciones
  @METHOD:  GET
*/
	UN_COMPANIA 	IN PCK_SUBTIPOS.TI_COMPANIA,
	UN_ANO     		IN PCK_SUBTIPOS.TI_ANIO,
	UN_VALOR		  IN VARCHAR2
)
RETURN PCK_SUBTIPOS.TI_LOGICO
	AS 
		MI_CAMPO 	  VARCHAR2(30 CHAR);
		MI_STRSQL 	PCK_SUBTIPOS.TI_STRSQL;
		MI_EXISTE 	VARCHAR2(1 CHAR);
	BEGIN 
		SELECT 	CASE UN_VALOR
					WHEN '0' THEN 'FECHA_CIERRE_ADQUISICIONES'
					WHEN '1' THEN 'FECHA_CIERRE_ADQUI_EJEC'	
					WHEN '2' THEN 'FECHA_CIERRE_PLAN_INDICATIVO'
					WHEN '3' THEN 'FECHA_CIERRE_PLAN_INDI_EJEC'
					WHEN '4' THEN 'FECHA_CIERRE_PLAN_ACCION'
					WHEN '5' THEN 'FECHA_CIERRE_PLAN_ACCION_EJEC'
				END FECHA
		INTO MI_CAMPO		
		FROM DUAL;	
		BEGIN 
			MI_STRSQL := '	SELECT DISTINCT ''X''  
							FROM   ANO    
							WHERE  COMPANIA   = '''||UN_COMPANIA||'''
								AND  NUMERO     =  '||UN_ANO||'
								AND '||MI_CAMPO||' >= SYSDATE';

			EXECUTE IMMEDIATE MI_STRSQL INTO MI_EXISTE;	
		EXCEPTION WHEN NO_DATA_FOUND THEN	
			RETURN -1 ;
		END;
	RETURN 0;
END FC_PLANADQUICERRADO;		


