											GROUP BY  	DETALLE_COMPROBANTE_PPTAL.COMPANIA,
														DETALLE_COMPROBANTE_PPTAL.ANO_AFECT,
														DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT,
														DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO,
														DETALLE_COMPROBANTE_PPTAL.CONSECUTIVOPPTO)  DISMINUCION_DEG
									ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA     = DISMINUCION_DEG.COMPANIA
									AND DETALLE_COMPROBANTE_PPTAL.ANO          = DISMINUCION_DEG.ANO_AFECT
									AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE    = DISMINUCION_DEG.TIPO_CPTE_AFECT
									AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE  = DISMINUCION_DEG.CMPTE_AFECTADO
									AND DETALLE_COMPROBANTE_PPTAL.CONSECUTIVO  = DISMINUCION_DEG.CONSECUTIVOPPTO 
