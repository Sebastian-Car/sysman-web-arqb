SELECT SUM(MOV_DEBITO-MOV_CREDITO) AS VLRCOMPROMISO,
 PACPROGRAMADO.COMPANIA,
 PACPROGRAMADO.ANO,
 PACPROGRAMADO.TIPO_CPTE,
 PACPROGRAMADO.COMPROBANTE,
TO_CHAR(PACPROGRAMADO.FECHA,'YYYY') AS ANOAPAGAR,
TO_CHAR(PACPROGRAMADO.FECHA,'MM') AS MESAPAGAR,
PCK_SYSMAN_UTL.FC_NOMBRE_MES(TO_CHAR(PACPROGRAMADO.FECHA,'MM')) || ' / ' || TO_CHAR(PACPROGRAMADO.FECHA,'YYYY') AS MESCOMPROMISO,
 PACPROGRAMADO.FECHA,
 DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO AS VLRTOTAL,
 DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO,
 DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO
FROM PACPROGRAMADO 
LEFT JOIN DETALLE_COMPROBANTE_PPTAL 
ON (PACPROGRAMADO.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA) 
AND (PACPROGRAMADO.ANO = DETALLE_COMPROBANTE_PPTAL.ANO) 
AND (PACPROGRAMADO.TIPO_CPTE = DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE) 
AND (PACPROGRAMADO.COMPROBANTE = DETALLE_COMPROBANTE_PPTAL.COMPROBANTE) 
AND (PACPROGRAMADO.CONSECUTIVO = DETALLE_COMPROBANTE_PPTAL.CONSECUTIVO)
WHERE PACPROGRAMADO.COMPANIA = $P{PR_COMPANIA}
  AND PACPROGRAMADO.ANO = $P{PR_ANO}
  AND PACPROGRAMADO.TIPO_CPTE = $P{PR_TIPO_CPTE}
  AND PACPROGRAMADO.COMPROBANTE BETWEEN $P{PR_COMPROBANTE} AND $P{PR_COMPROBANTE}
GROUP BY PACPROGRAMADO.COMPANIA,
 PACPROGRAMADO.ANO,
 PACPROGRAMADO.TIPO_CPTE, PACPROGRAMADO.COMPROBANTE,
 TO_CHAR(PACPROGRAMADO.FECHA,'YYYY'),
TO_CHAR(PACPROGRAMADO.FECHA,'MM'), 
 PACPROGRAMADO.FECHA, 
 DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO, 
 DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO
ORDER BY PACPROGRAMADO.COMPANIA,
 PACPROGRAMADO.ANO,
 PACPROGRAMADO.TIPO_CPTE,
 PACPROGRAMADO.COMPROBANTE,
 TO_CHAR(PACPROGRAMADO.FECHA,'YYYY'),
TO_CHAR(PACPROGRAMADO.FECHA,'MM')