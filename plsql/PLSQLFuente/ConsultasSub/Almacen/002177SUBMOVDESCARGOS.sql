SELECT CASE WHEN TIPOMOVIMIENTO.CLASE = 'D' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END AS DESCARGOS,
    CASE WHEN TIPOMOVIMIENTO.CLASE = 'D' THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END AS CANTDESCARGOS,
    TO_CHAR (D_MOVIMIENTO.FECHA,'DD/MM/YYYY')FECHA,
    D_MOVIMIENTO.ELEMENTO,
    D_MOVIMIENTO.CANTIDAD,
    D_MOVIMIENTO.CLASEDESCARGO AS TIPOMOVIMIENTO, 
    D_MOVIMIENTO.DESCARGO AS MOVIMIENTO,
    CASE WHEN 's$claseElemento$s' = 2 THEN  2 ELSE IND_REG END AS REGIS,
    CASE WHEN CASE WHEN '2' = 2 THEN  2 ELSE IND_REG END = 2 THEN 'SI' ELSE 'NO' END AS VALIDACION,
    D_MOVIMIENTO.IND_REG, 
    TIPOMOVIMIENTO.NOMBRECORTO,
    D_MOVIMIENTO.ELEMENTO AS CODIGOELEMENTO, 
    INVENTARIO.NOMBRELARGO
FROM D_MOVIMIENTO 
LEFT JOIN INVENTARIO 
    ON D_MOVIMIENTO.COMPANIA = INVENTARIO.COMPANIA
    AND D_MOVIMIENTO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
LEFT JOIN TIPOMOVIMIENTO 
    ON D_MOVIMIENTO.COMPANIA = TIPOMOVIMIENTO.COMPANIA 
    AND D_MOVIMIENTO.CLASEDESCARGO = TIPOMOVIMIENTO.CODIGO
WHERE D_MOVIMIENTO.COMPANIA = 's$compania$s'
AND  CASE WHEN 's$claseElemento$s' = 2 THEN  2 ELSE Ind_Reg END = s$claseElemento$s
    AND D_MOVIMIENTO.FECHA  BETWEEN s$fechaInicial$s  AND s$fechaFinal$s
    AND D_MOVIMIENTO.ELEMENTO BETWEEN s$elementoInicial$s And s$elementoFinal$s
GROUP BY CASE WHEN TIPOMOVIMIENTO.CLASE = 'D' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END,
    CASE WHEN TIPOMOVIMIENTO.CLASE = 'D' THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END,
    D_MOVIMIENTO.FECHA,
    D_MOVIMIENTO.COMPANIA, 
    D_MOVIMIENTO.ELEMENTO,
    D_MOVIMIENTO.CANTIDAD, 
    D_MOVIMIENTO.CLASEDESCARGO,
    D_MOVIMIENTO.DESCARGO, 
    CASE WHEN 's$claseElemento$s' = 2 THEN  2 ELSE IND_REG END,
    D_MOVIMIENTO.IND_REG, 
    TIPOMOVIMIENTO.NOMBRECORTO, 
    D_MOVIMIENTO.ELEMENTO,
    INVENTARIO.NOMBRELARGO, 
    TIPOMOVIMIENTO.NOMBRE 
ORDER BY D_MOVIMIENTO.CLASEDESCARGO,
    D_MOVIMIENTO.DESCARGO