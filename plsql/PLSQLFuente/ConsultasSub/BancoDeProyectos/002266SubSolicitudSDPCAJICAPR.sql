SELECT DISTINCT 
	BP_D_NOVEDADPROYECTO.RUBROPRESUPUESTAL RUBRO,
	BP_D_NOVEDADPROYECTO.FUENTERECURSOS FUENTE,
	PLAN_PRESUPUESTAL.NOMBRE NOMBRE_RUBRO,
	BP_D_NOVEDADPROYECTO.VALORSOLICITADO
	FROM
	BPNOVEDADPROYECTO
	INNER JOIN BP_D_NOVEDADPROYECTO ON
	BPNOVEDADPROYECTO.COMPANIA = BP_D_NOVEDADPROYECTO.COMPANIA
	AND BPNOVEDADPROYECTO.CODIGO = BP_D_NOVEDADPROYECTO.NOVEDAD
	AND BPNOVEDADPROYECTO.TIPOT = BP_D_NOVEDADPROYECTO.TIPOT
	AND BPNOVEDADPROYECTO.CLASET = BP_D_NOVEDADPROYECTO.CLASET
	AND BPNOVEDADPROYECTO.DEPENDENCIA = BP_D_NOVEDADPROYECTO.DEPENDENCIA
	INNER JOIN PLAN_PRESUPUESTAL ON
	BP_D_NOVEDADPROYECTO.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
	AND BP_D_NOVEDADPROYECTO.RUBROPRESUPUESTAL = PLAN_PRESUPUESTAL.CODIGO
	AND BP_D_NOVEDADPROYECTO.ANORUBRO = PLAN_PRESUPUESTAL.ANO
	WHERE
		BP_D_NOVEDADPROYECTO.COMPANIA = '$P!{PR_COMPANIA}'
	AND BP_D_NOVEDADPROYECTO.TIPOT = '$P!{PR_TIPOT}'
	AND BP_D_NOVEDADPROYECTO.CLASET = '$P!{PR_CLASET}'
	AND BPNOVEDADPROYECTO.CODIGO = '$P!{PR_CONSECUTIVO}'
	AND BPNOVEDADPROYECTO.DEPENDENCIA = '$P!{PR_DEPENDENCIA}'