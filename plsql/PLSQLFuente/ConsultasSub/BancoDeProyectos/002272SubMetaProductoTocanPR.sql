SELECT DISTINCT 
INDICAPRODUCTO.DESCRIPCION PRODUCTO
FROM
	BP_D_NOVEDADPROYECTO
LEFT JOIN BP_PLAN_INDICATIVO INDICAPRODUCTO
   ON BP_D_NOVEDADPROYECTO.COMPANIA                        = INDICAPRODUCTO.COMPANIA 
   AND SUBSTR(BP_D_NOVEDADPROYECTO.ID_META_PRODUCTO,1,'s$meta$s') = INDICAPRODUCTO.ID
   AND BP_D_NOVEDADPROYECTO.VIGENCIA_PLAN_INDICATIVO        = INDICAPRODUCTO.VIGENCIA_INICIAL
WHERE
	BP_D_NOVEDADPROYECTO.COMPANIA = '$P!{PR_COMPANIA}'
	AND BP_D_NOVEDADPROYECTO.TIPOT = '$P!{PR_TIPOT}'
	AND BP_D_NOVEDADPROYECTO.CLASET = '$P!{PR_CLASET}'
	AND BP_D_NOVEDADPROYECTO.NOVEDAD = '$P!{PR_CONSECUTIVO}'