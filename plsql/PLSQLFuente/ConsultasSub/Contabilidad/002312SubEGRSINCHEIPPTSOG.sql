SELECT DC.COMPANIA  COMP, 
    DC.ANO  ANOCOMP, 
    DC.TIPO_CPTE, 
    DC.COMPROBANTE, 
    DC.AUXILIAR, 
    DC.CENTRO_COSTO, 
    PLAN_PRESUPUESTAL.NOMBRE AS PRIMERODENOMBRE, 
    DC.CUENTA AS CUENTA, 
    DC2.COMPROBANTE AS NUMERORES, 
    DC2.CMPTE_AFECTADO AS NUMERODIS, 
    SUM(DC.VALOR_DEBITO-DC.VALOR_CREDITO) AS VALORNETO, 
    DC2.TIPO_CPTE, DC2.COMPROBANTE AS NUMERORES1, 
    DC2.TIPO_CPTE_AFECT, 
    DC2.CMPTE_AFECTADO AS NUMERODIS1, 
    DC1.TIPO_CPTE, 
    DC1.COMPROBANTE AS NORES, 
    DC1.TIPO_CPTE_AFECT, 
    DC1.CMPTE_AFECTADO AS NODIS, 
    DC.CUENTA AS CUENTACOMPLETA, 
    DC1.CODIGO_CPC
    FROM 
    DETALLE_COMPROBANTE_PPTAL  DC
    INNER JOIN DETALLE_COMPROBANTE_PPTAL  DC1 
    ON DC.ANO = DC1.ANO 
    AND DC.COMPANIA = DC1.COMPANIA 
    AND DC.TIPO_CPTE_AFECT = DC1.TIPO_CPTE 
    AND DC.CMPTE_AFECTADO = DC1.COMPROBANTE 
    AND DC.CONSECUTIVOPPTO = DC1.CONSECUTIVO 
    INNER JOIN DETALLE_COMPROBANTE_PPTAL  DC2 
    ON DC1.CONSECUTIVOPPTO = DC2.CONSECUTIVO 
    AND DC1.CMPTE_AFECTADO = DC2.COMPROBANTE 
    AND DC1.TIPO_CPTE_AFECT = DC2.TIPO_CPTE 
    AND DC1.ANO = DC2.ANO
    AND DC1.COMPANIA = DC2.COMPANIA 
    INNER JOIN PLAN_PRESUPUESTAL 
    ON DC.CUENTA = PLAN_PRESUPUESTAL.CODIGO 
    AND DC.ANO = PLAN_PRESUPUESTAL.ANO 
    AND DC.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
WHERE 
    DC.COMPANIA= $P{PR_COMPANIA} 
    and DC.ANO= $P{PR_ANO}
    and DC.TIPO_CPTE = $P{PR_TIPO}
    and DC.COMPROBANTE between  $P{PR_COMPROBANTE} and $P{PR_COMPROBANTE}

GROUP BY 
    DC.COMPANIA, 
    DC.ANO, 
    DC.TIPO_CPTE, 
    DC.COMPROBANTE, 
    DC.AUXILIAR, 
    DC.CENTRO_COSTO, 
    DC.CUENTA, 
    DC2.TIPO_CPTE, 
    DC2.COMPROBANTE, 
    DC2.TIPO_CPTE_AFECT, 
    DC2.CMPTE_AFECTADO, 
    DC1.TIPO_CPTE, 
    DC1.COMPROBANTE, 
    DC1.TIPO_CPTE_AFECT, 
    DC1.CMPTE_AFECTADO, 
    DC.CUENTA, 
    DC1.CODIGO_CPC,
    PLAN_PRESUPUESTAL.NOMBRE