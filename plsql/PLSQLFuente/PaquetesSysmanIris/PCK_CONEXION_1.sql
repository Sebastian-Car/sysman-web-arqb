CREATE OR REPLACE PACKAGE BODY                "PCK_CONEXION" 
AS

FUNCTION FC_GETUSER
RETURN VARCHAR2 AS
  BEGIN
    RETURN GL_USER;
END FC_GETUSER;

FUNCTION FC_GETIP
RETURN VARCHAR2 AS
  BEGIN
    RETURN GL_IPCOMPT;
  END FC_GETIP;

FUNCTION FC_GETFORM_MENU
RETURN VARCHAR2 AS
  BEGIN
    RETURN GL_FORM_MENU;
  END FC_GETFORM_MENU;
  
PROCEDURE PR_SETUSER
(
  UN_USUARIO VARCHAR2
) AS
  BEGIN
    GL_USER := UN_USUARIO;
  END PR_SETUSER;

  PROCEDURE PR_SETIP(
  UN_IP VARCHAR2
) AS
  BEGIN
    GL_IPCOMPT := UN_IP;
  END PR_SETIP;

  PROCEDURE PR_SETFORM_MENU(
  UN_FORM_MENU VARCHAR2
) AS
  BEGIN
    GL_FORM_MENU := UN_FORM_MENU;
  END PR_SETFORM_MENU;
  
END PCK_CONEXION;