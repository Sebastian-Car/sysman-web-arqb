    NAME              : PADC
    AUTHORS           : SYSMAN  SAS
    AUTHOR MIGRACION  : JOSE PASCUAL GÓMEZ BLANCO
    DATE MIGRADOR     : 15/08/2015
    TIME              : 08:13 AM
    SOURCE MODULE     : NominaP2015.04.01DUI.accdb
    MODIFIER          : 
