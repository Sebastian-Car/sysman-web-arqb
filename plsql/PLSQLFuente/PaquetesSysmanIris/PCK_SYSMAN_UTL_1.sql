FUNCTION FC_COPIAR_PLANTILLA (
    /*
        NAME              :  FC_COPIAR_PLANTILLA
        AUTHORS           : LVEGA
        DATE MIGRADOR     : 16/10/2025
        MODIFIER          :
        DATE MODIFIED     :
        TIME              :
        DESCRIPTION       : Copia informacion entre las tablas 
                            MODELO_PLANTILLA, MODELO_TABLA Y MODELO_VARIABLES
    */
    UN_PLANTILLA_ORIGEN  IN VARCHAR2,
    UN_PLANTILLA_DESTINO IN VARCHAR2,
    UN_FECHA_ORIGEN      IN DATE,
    UN_NOMBRE            IN VARCHAR2,
    UN_TIPO              IN NUMBER,
    UN_PLANTILLA         IN VARCHAR2
) RETURN VARCHAR2
AS
    MI_CONSECUTIVO  NUMBER;
    MI_CAMPOS       PCK_SUBTIPOS.TI_CAMPOS;
    MI_VALORES      PCK_SUBTIPOS.TI_VALORES;
    MI_CONDICION    PCK_SUBTIPOS.TI_CONDICION;
    MI_TABLA        VARCHAR2(200 CHAR);
    MI_CONSULTA     PCK_SUBTIPOS.TI_MERGEUSING;
    MI_ENLACE       PCK_SUBTIPOS.TI_MERGEENLACE;
    MI_EXISTE       PCK_SUBTIPOS.TI_MERGEEXISTE;
    MI_RTA          VARCHAR2(32000 CHAR);

BEGIN
    EXECUTE IMMEDIATE 'ALTER SESSION SET TIME_ZONE=''-05:00''';

    ---------------------------------------------------------------------
    -- 0. ELIMINAR MODELO_VARIABLES DE PLANTILLA DESTINO
    ---------------------------------------------------------------------
    BEGIN
        MI_CONDICION := 'PLANTILLA = ''' || UN_PLANTILLA_DESTINO || '''';

        MI_RTA := PCK_DATOS.FC_ACME(
                      UN_TABLA      => 'MODELO_VARIABLES',
                      UN_ACCION     => 'E',
                      UN_CONDICION  => MI_CONDICION
                  );

    EXCEPTION
        WHEN PCK_EXCEPCIONES.EXC_ELIMINAR THEN
            RAISE PCK_EXCEPCIONES.EXC_ALMACEN;
    END;

    ---------------------------------------------------------------------
    -- 1. COPIAR MODELO_PLANTILLA (MERGE)
    ---------------------------------------------------------------------
    BEGIN
        MI_TABLA := 'MODELO_PLANTILLA';

        MI_CONSULTA :=
            'SELECT ' ||
                UN_PLANTILLA_DESTINO || ' AS CODIGO,
                ''' || UN_NOMBRE || ''' NOMBRE,
                ''' || UN_PLANTILLA || ''' PLANTILLA,
                CONSULTA,
                ''' || UN_TIPO || ''' TIPO
            FROM MODELO_PLANTILLA
            WHERE CODIGO = ' || UN_PLANTILLA_ORIGEN || '
              AND TRUNC(FECHA) = TRUNC(TO_DATE(''' || UN_FECHA_ORIGEN || '''))';

        MI_ENLACE := 'TABLA.CODIGO = VISTA.CODIGO';

        MI_EXISTE :=
            'UPDATE SET 
                TABLA.CONSULTA  = VISTA.CONSULTA,
                TABLA.PLANTILLA = VISTA.PLANTILLA';

        MI_RTA := PCK_DATOS.FC_ACME(
                      UN_TABLA        => MI_TABLA,
                      UN_ACCION       => 'MM',
                      UN_MERGEUSING   => MI_CONSULTA,
                      UN_MERGEENLACE  => MI_ENLACE,
                      UN_MERGEEXISTE  => MI_EXISTE
                  );

    EXCEPTION
        WHEN PCK_EXCEPCIONES.EXC_MERGE THEN
            RAISE PCK_EXCEPCIONES.EXC_ALMACEN;
    END;

      
    SELECT NVL(MAX(CODIGO), 0) + 1
    INTO MI_CONSECUTIVO
    FROM MODELO_TABLA;

    ---------------------------------------------------------------------
    -- 2. COPIAR MODELO_TABLA
    ---------------------------------------------------------------------
    FOR R IN (
        SELECT NOMBRE,
               CONSULTA,
               ENLACE_PRINCIPAL,
               ENLACE_SECUNDARIO,
               CONDICION,
               CREATED_BY,
               MODIFIED_BY,
               TIPO_VARIABLES_CONSULTA,
               MODO_CREACION
        FROM MODELO_TABLA
        WHERE PLANTILLA = UN_PLANTILLA_ORIGEN
          AND TRUNC(FECHA) = UN_FECHA_ORIGEN
    ) LOOP
        INSERT INTO MODELO_TABLA (
            PLANTILLA,
            CODIGO,
            NOMBRE,
            CONSULTA,
            ENLACE_PRINCIPAL,
            ENLACE_SECUNDARIO,
            CONDICION,
            CREATED_BY,
            MODIFIED_BY,
            TIPO_VARIABLES_CONSULTA,
            FECHA,
            DATE_MODIFIED,
            DATE_CREATED,
            MODO_CREACION
        )
        VALUES (
            UN_PLANTILLA_DESTINO,
            MI_CONSECUTIVO,
            SUBSTR(R.NOMBRE, 1, INSTR(R.NOMBRE, '#>') - 4) || '#>',
            R.CONSULTA,
            R.ENLACE_PRINCIPAL,
            R.ENLACE_SECUNDARIO,
            R.CONDICION,
            R.CREATED_BY,
            R.MODIFIED_BY,
            R.TIPO_VARIABLES_CONSULTA,
            (SELECT FECHA FROM MODELO_PLANTILLA WHERE CODIGO = UN_PLANTILLA_DESTINO),
            SYSDATE,
            SYSDATE,
            R.MODO_CREACION
        );

        MI_CONSECUTIVO := MI_CONSECUTIVO + 1;
    END LOOP;

    SELECT NVL(MAX(CODIGO), 0) + 1
    INTO MI_CONSECUTIVO
    FROM MODELO_VARIABLES;

    ---------------------------------------------------------------------
    -- 3. COPIAR MODELO_VARIABLES
    ---------------------------------------------------------------------
    FOR V IN (
        SELECT NOMBRE,
               TIPO,
               FORMATO,
               ETIQUETA,
               DESCRIPCION,
               CREATED_BY,
               MODIFIED_BY
        FROM MODELO_VARIABLES
        WHERE PLANTILLA = UN_PLANTILLA_ORIGEN
        AND TRUNC(FECHA) = TRUNC(UN_FECHA_ORIGEN)
    ) LOOP
        INSERT INTO MODELO_VARIABLES (
            PLANTILLA,
            CODIGO,
            NOMBRE,
            TIPO,
            FORMATO,
            ETIQUETA,
            DESCRIPCION,
            FECHA,
            CREATED_BY,
            MODIFIED_BY,
            DATE_CREATED,
            DATE_MODIFIED
        )
        VALUES (
            UN_PLANTILLA_DESTINO,
            MI_CONSECUTIVO,
            V.NOMBRE,
            V.TIPO,
            V.FORMATO,
            V.ETIQUETA,
            CASE
                WHEN V.DESCRIPCION IS NULL THEN ''
                ELSE SUBSTR(V.DESCRIPCION, 1, INSTR(V.DESCRIPCION, '#>') - 4) || '#>'
            END,
            (SELECT FECHA 
               FROM MODELO_PLANTILLA 
              WHERE CODIGO = UN_PLANTILLA_DESTINO),
            V.CREATED_BY,
            V.MODIFIED_BY,
            SYSDATE,
            SYSDATE
        );

        MI_CONSECUTIVO := MI_CONSECUTIVO + 1;
    END LOOP;

    COMMIT;
    RETURN 'Proceso completado correctamente para la plantilla destino ' || UN_PLANTILLA_DESTINO;

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        RETURN 'Error al copiar modelos: ' || SQLERRM;
END FC_COPIAR_PLANTILLA;

