*/
(
  UN_NUMT NUMBER,
  UN_CTVS NUMBER DEFAULT 0
)
RETURN VARCHAR2
AS
    MI_UN              VARCHAR2(255); --' CADENA DE CARACTERES PARA LAS UNIDADES
    MI_DEC             VARCHAR2(255); --' CADENA DE CARACTERES PARA LAS DECENAS
    MI_CEN             VARCHAR2(255); --' CADENA DE CARACTERES PARA LAS CENTENAS
    MI_MIL             VARCHAR2(255); --' NUMERO DE UNIDADES DE MIL DEL PARAMETRO
    MI_NNU             VARCHAR2(255); --' NUMERO DE UNIDADES DEL PARAMETRO
    MI_LETRAS          VARCHAR2(255); --' CADENA DE CARACTERES DE RESULTADO
    MI_PARTE           VARCHAR2(255); --' NUMERO DE ITERACIONES
