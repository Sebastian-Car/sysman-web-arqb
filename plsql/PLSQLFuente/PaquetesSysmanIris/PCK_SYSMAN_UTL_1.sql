    DESCRIPTION       : DEVUELVE EL DIGITO DE VERIFICACION DE UN TERCERO
                        
  */ 
  UN_NUMERO   VARCHAR2 DEFAULT '0'
  )  
  RETURN NUMBER
  DETERMINISTIC
  AS
  ACUM     NUMBER;
  MI_NUMERO NUMBER;
  NTL      VARCHAR2(32000);
  MI_DIGITO NUMBER;
  TYPE DCH1 is VARRAY(15) OF INTEGER;
  DCH2 DCH1; 
  BEGIN
    BEGIN
    
    
   MI_NUMERO:=TRUNC(TO_NUMBER(UN_NUMERO)); 
   
