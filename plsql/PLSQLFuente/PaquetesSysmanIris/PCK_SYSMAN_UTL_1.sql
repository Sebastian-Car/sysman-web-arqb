    MI_PIX             VARCHAR2(255); --' POSICION DENTRO DE UNA CADENA
    MI_PF              VARCHAR2(255); --'-- POSICION FINAL DENTRO DE UNA CADENA
    MI_TB              VARCHAR2(255); --' CADENA DE CARACTERES TEMPORAL
    MI_DECT            VARCHAR2(255); --' DECIMALES DENTRO DE CADA TRIPLA
    MI_SD              VARCHAR2(255); --' POSICION DENTRO DE UNA CADENA
    MI_SW              NUMBER;        --' INDICADOR DE ITERACION LOOP
    MI_MONEDACOPIA     VARCHAR2(255);
    MI_OTRAMONEDACOPIA VARCHAR2(255);
    MI_INTUBICACION    NUMBER;
    MI_KBILLON         NUMBER:= 1000000000;
    MI_KMILLON         NUMBER:= 1000000;
    MI_KMIL            NUMBER:= 1000;
    MI_KCIEN           NUMBER:= 100;
    MI_KDIEZ           NUMBER:= 10;
    MI_VALORLETRAS     VARCHAR2(2000);
    MI_NUMT            NUMBER;
    MI_CTVS            NUMBER;
  BEGIN
    IF UN_NUMT < 0 THEN
      MI_NUMT := -UN_NUMT;
    ELSE
      MI_NUMT := UN_NUMT;
    END IF;
    
   
    MI_NUMT          := ROUND(MI_NUMT, 2);
    MI_LETRAS        := '';
    MI_UN            := '1UN 2DOS 3TRES 4CUATRO 5CINCO 6SEIS 7SIETE 8OCHO 9NUEVE 10DIEZ ';
    MI_DEC           := '11ONCE 12DOCE 13TRECE 14CATORCE 15QUINCE 10DIEZ 20VEINTI 30TREINTA 40CUARENTA 50CINCUENTA 60SESENTA 70SETENTA 80OCHENTA 90NOVENTA ';
