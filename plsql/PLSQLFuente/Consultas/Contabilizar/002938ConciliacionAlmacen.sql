MERGE INTO CONSULTAS FIN USING (SELECT '002938ConciliacionAlmacen' INFORME ,TO_CLOB(q'[SELECT MOVIMIENTO.COMPANIA,TO_NUMBER(TO_CHAR(D_MOVIMIENTO.FECHA,'YYYY')) ANO,
           D_MOVIMIENTO.FECHA,ALMACENCONTABILIDAD.CODIGOELEMENTO GRUPO,
           D_MOVIMIENTO.ELEMENTO,
           INVENTARIO.NOMBRELARGO || ' ' ||D_MOVIMIENTO.ESPECIFICACION DESCRIPCION_DEL_ELEMENTO,
           D_MOVIMIENTO.SERIE,MOVIMIENTO.DESCRIPCION,
           ALMACENCONTABILIDAD.TIPOMOVIMIENTO,
           TIPOMOVIMIENTO.NOMBRE NOMTIPO,
           D_MOVIMIENTO.MOVIMIENTO,
           PCK_SYSMAN_UTL.FC_PAR(D_MOVIMIENTO.COMPANIA,'TIPO COMPROBANTE INTERFASE DIARIA ALMACEN',1,D_MOVIMIENTO.FECHA) TIPO_CPTE,
           CONCAT(s$ano$s,TO_CHAR(D_MOVIMIENTO.FECHA,'MM'))||'00'||TO_CHAR(D_MOVIMIENTO.FECHA,'DD') COMPROBANTE,
           CASE WHEN 0 NOT IN(0) THEN  NIIF_CUENTADEBITO   ELSE  ALMACENCONTABILIDAD.CUENTADEBITO   END  CUENTADEBITO, 
           D_MOVIMIENTO.VALORTOTAL  VALORDEBITO ,
           CASE WHEN 0 NOT IN(0) THEN  NIIF_CUENTACREDITO  ELSE  ALMACENCONTABILIDAD.CUENTACREDITO END   CUENTACREDITO,  
            D_MOVIMIENTO.VALORTOTAL  VALORCREDITO
     FROM   MOVIMIENTO 
        INNER JOIN D_MOVIMIENTO
            ON MOVIMIENTO.COMPANIA       = D_MOVIMIENTO.COMPANIA
            AND MOVIMIENTO.TIPOMOVIMIENTO = D_MOVIMIENTO.TIPOMOVIMIENTO
            AND MOVIMIENTO.NUMERO         = D_MOVIMIENTO.MOVIMIENTO
        INNER JOIN TIPOMOVIMIENTO 
            ON TIPOMOVIMIENTO.COMPANIA = D_MOVIMIENTO.COMPANIA
            AND TIPOMOVIMIENTO.CODIGO   = D_MOVIMIENTO.TIPOMOVIMIENTO
        LEFT JOIN ALMACENCONTABILIDAD 
            ON D_MOVIMIENTO.COMPANIA                          = ALMACENCONTABILIDAD.COMPANIA
            AND TO_NUMBER(TO_CHAR(D_MOVIMIENTO.FECHA,'YYYY')) = ALMACENCONTABILIDAD.ANO
            AND D_MOVIMIENTO.TIPOMOVIMIENTO                    = ALMACENCONTABILIDAD.TIPOMOVIMIENTO
            AND SUBSTR(D_MOVIMIENTO.ELEMENTO,1,LENGTH(ALMACENCONTABILIDAD.CODIGOELEMENTO)) = ALMACENCONTABILIDAD.CODIGOELEMENTO
        INNER JOIN INVENTARIO 
            ON D_MOVIMIENTO.COMPANIA = INVENTARIO.COMPANIA
            AND D_MOVIMIENTO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
    WHERE D_MOVIMIENTO.COMPANIA  = s$compania$s
        AND D_MOVIMIENTO.FECHA BETWEEN TO_DATE('s$fechas$s','dd/MM/yyyy') and TO_DATE('s$fechafinal$s','dd/MM/yyyy')
        AND IND_REG  NOT IN(0)
        AND TO_CHAR(D_MOVIMIENTO.FECHA,'YYYY') = s$ano$s
        AND NOT (TIPOMOVIMIENTO.CONCEPTO IN ('T') AND TIPOMOVIMIENTO.CLASE IN('T'))
        AND LENGTH(ALMACENCONTABILIDAD.CODIGOELEMENTO) = s$agrupacion$s
    ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.ELEMENTO]') CONSULTA, 96 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);