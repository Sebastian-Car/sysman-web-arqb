             CONCEPTOS.TERCERO,
             CONCEPTOS.SUCURSAL,
             TERCERO.NOMBRE,
             CONCEPTOS.ID_DE_CONCEPTO,
             CASE WHEN CONCEPTOS.CLASE='3'
             THEN 'REMUNERACION'
             ELSE 
             CASE WHEN CONCEPTOS.CLASE='5'
                 THEN 'DESCUENTOS'
             ELSE 
               CASE WHEN CONCEPTOS.CLASE='7'
                 THEN 'NETO_PAGAR'
             ELSE 
                  CASE WHEN CONCEPTOS.CLASE='8'
                 THEN 'CONTABLES'
                END 
              END
            END
           END  CLASE_CONCEPTO,
       HISTORICOS.VALOR 
     FROM CONCEPTOS INNER JOIN HISTORICOS
     ON  CONCEPTOS.COMPANIA         =HISTORICOS.COMPANIA
     AND CONCEPTOS.ID_DE_CONCEPTO   =HISTORICOS.ID_DE_CONCEPTO
    INNER JOIN PERSONAL  
     ON  HISTORICOS.COMPANIA         =PERSONAL.COMPANIA
     AND HISTORICOS.ID_DE_EMPLEADO   =PERSONAL.ID_DE_EMPLEADO
    LEFT JOIN TERCERO 
      ON  CONCEPTOS.COMPANIA  =TERCERO.COMPANIA
      AND CONCEPTOS.TERCERO    =TERCERO.NIT
      AND CONCEPTOS.SUCURSAL  =TERCERO.SUCURSAL
  WHERE CONCEPTOS.COMPANIA='s$compania$s'
   AND CONCEPTOS.CLASE IN(3,5,7,8)
   AND HISTORICOS.ANO=s$ano$s
   AND HISTORICOS.MES=s$mesFinal$s
   AND HISTORICOS.PERIODO=s$periodo$s
   AND HISTORICOS.ID_DE_PROCESO=s$procesos$s
   AND TERCERO.COMPANIA IS NULL
ORDER BY CONCEPTOS.TERCERO]') CONSULTA, 96 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);