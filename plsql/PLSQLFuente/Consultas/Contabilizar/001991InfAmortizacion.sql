SELECT SF_DETALLE_ABONO.CUOTA,
  (SF_ABONOS.DEUDA_TOTAL - SF_ABONOS.CUOTA_INICIAL) -    
  CASE WHEN CUOTA = 1 THEN 0 
  ELSE SUM(SF_DETALLE_ABONO.CAPITAL ) OVER (ORDER BY CUOTA ROWS UNBOUNDED PRECEDING)-SF_DETALLE_ABONO.CAPITAL END DEUDACAPITAL,
  SF_DETALLE_ABONO.TOTAL_CUOTA        VALORCUOTA,
  SF_DETALLE_ABONO.INT_FINANCIACION   ABONOINTERESES,
  SF_DETALLE_ABONO.CAPITAL            ABONOCAPITAL
FROM SF_DETALLE_ABONO
INNER JOIN SF_ABONOS
ON SF_DETALLE_ABONO.COMPANIA =    SF_ABONOS.COMPANIA
AND SF_DETALLE_ABONO.TIPO_ABONO =  SF_ABONOS.TIPO
AND SF_DETALLE_ABONO.CODIGO_ABONO = SF_ABONOS.CODIGO
WHERE SF_DETALLE_ABONO.COMPANIA  = s$compania$s
AND SF_DETALLE_ABONO.CODIGO_ABONO = s$abono$s
AND SF_DETALLE_ABONO.CUOTA   <>0
