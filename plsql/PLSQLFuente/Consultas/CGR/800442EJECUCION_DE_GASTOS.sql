SELECT 
CODIGO,
s$nombre$s
VIGENCIAGASTO,
SECCION_PPTAL_CUIPO,
SECTOR_CUIPO,
CPC,
FUENTE_FINANCIACION,
PROGRAMATICO,
BPIN,
SITUACION_FONDOS,
POLITICA_PUBLICA,
TERCERO,
COMPROMISOS,
OBLIGACIONES,
PAGOS
FROM(
SELECT 
CUIPO_CODIGO_CCEPT.COMPANIA,
CUIPO_CODIGO_CCEPT.ANO,
CUIPO_CODIGO_CCEPT.CODIGO,
CUIPO_CODIGO_CCEPT.NOMBRE,
PLAN_PPTAL_CONFIG.VIGENCIAGASTO,
PLAN_PPTAL_CONFIG.SECCION_PPTAL_CUIPO,
PLAN_PPTAL_CONFIG.SECTOR_CUIPO,
NVL(DETALLE_COMPROBANTE_PPTAL.CODIGO_CPC,'0.0') CPC,
NVL(DETALLE_COMPROBANTE_PPTAL.FUENTE_CUIPO,'0.0') FUENTE_FINANCIACION,
CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN PLAN_PPTAL_CONFIG.TIPO_NORMA ELSE '0.0' END TIPO_NORMA,
CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN PLAN_PPTAL_CONFIG.NUMERO_NORMA ELSE '0.0' END NUMERO_NORMA,
CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN TO_CHAR(PLAN_PPTAL_CONFIG.FECHA_NORMA,'DD/MM/YYYY') ELSE '01/01/1900' END FECHA_NORMA,
PLAN_PPTAL_CONFIG.PROGRAMA_CUIPO PROGRAMATICO,
PLAN_PPTAL_CONFIG.CODIGO_BPIN_SGR  BPIN,
CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN '1.0' ELSE '2.0' END SITUACION_FONDOS,
NVL(PLAN_PPTAL_CONFIG.POLITCA_PUBLICA_CUIPO,'0') POLITICA_PUBLICA,
NVL(TERCERO_CHIP,1) TERCERO,
 SUM(CASE  WHEN TIPO_COMPROBPP.CLASE IN ('RES','ADR','DMR')
            THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)
            ELSE 0   
            END) AS COMPROMISOS,
            
         SUM(CASE  WHEN TIPO_COMPROBPP.CLASE IN ('REO','ARO','DRO')
              THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)
              ELSE 0   
              END) AS OBLIGACIONES,
          
             SUM(CASE  WHEN TIPO_COMPROBPP.CLASE IN ('EGR','ADG','DEG')
            THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)
            ELSE 0   
            END) AS PAGOS
  FROM CUIPO_CODIGO_CCEPT 
INNER JOIN DETALLE_COMPROBANTE_PPTAL
 ON CUIPO_CODIGO_CCEPT.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA
AND CUIPO_CODIGO_CCEPT.ANO      = DETALLE_COMPROBANTE_PPTAL.ANO
AND CUIPO_CODIGO_CCEPT.CODIGO   = DETALLE_COMPROBANTE_PPTAL.CUENTA
INNER JOIN TIPO_COMPROBPP
 ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA
AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
INNER JOIN PLAN_PPTAL_CONFIG
 ON CUIPO_CODIGO_CCEPT.COMPANIA = PLAN_PPTAL_CONFIG.COMPANIA
AND CUIPO_CODIGO_CCEPT.ANO      = PLAN_PPTAL_CONFIG.ANO
AND CUIPO_CODIGO_CCEPT.CODIGO   = PLAN_PPTAL_CONFIG.CODIGO
INNER JOIN PLAN_PRESUPUESTAL P 
 ON PLAN_PPTAL_CONFIG.COMPANIA = P.COMPANIA
AND PLAN_PPTAL_CONFIG.ANO      = P.ANO
AND PLAN_PPTAL_CONFIG.CODIGO   = P.CODIGO 
  WHERE P.COMPANIA = s$compania$s
    AND P.ANO =  s$anio$s
    AND DETALLE_COMPROBANTE_PPTAL.MES  <= s$mesfinal$s
    AND P.NATURALEZA ='D'
    AND P.REGALIAS IN(0)
    AND P.MOVIMIENTO NOT IN (0)
group by CUIPO_CODIGO_CCEPT.COMPANIA, CUIPO_CODIGO_CCEPT.ANO, CUIPO_CODIGO_CCEPT.CODIGO, CUIPO_CODIGO_CCEPT.NOMBRE, PLAN_PPTAL_CONFIG.VIGENCIAGASTO, PLAN_PPTAL_CONFIG.PROGRAMA_CUIPO ,
PLAN_PPTAL_CONFIG.CODIGO_BPIN_SGR  ,PLAN_PPTAL_CONFIG.SECCION_PPTAL_CUIPO, PLAN_PPTAL_CONFIG.SECTOR_CUIPO, NVL(DETALLE_COMPROBANTE_PPTAL.CODIGO_CPC,'0.0'), NVL(DETALLE_COMPROBANTE_PPTAL.FUENTE_CUIPO,'0.0'), CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN PLAN_PPTAL_CONFIG.TIPO_NORMA ELSE '0.0' END, 
CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN PLAN_PPTAL_CONFIG.NUMERO_NORMA ELSE '0.0' END, CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN TO_CHAR(PLAN_PPTAL_CONFIG.FECHA_NORMA,'DD/MM/YYYY') ELSE '01/01/1900' END, '', '', CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN '1.0' ELSE '2.0' END, 
NVL(PLAN_PPTAL_CONFIG.POLITCA_PUBLICA_CUIPO,'0'), NVL(TERCERO_CHIP,1) 
) CUIPO 
ORDER BY CODIGO