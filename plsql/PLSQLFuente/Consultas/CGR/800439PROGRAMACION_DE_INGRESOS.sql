SELECT CODIGO,
s$nombre$s
PRESUPUESTO_INICIAL,
PRESUPUESTO_DEFINITIVO
FROM (
SELECT CUIPO_CODIGO_CCEPT.CODIGO,
  CUIPO_CODIGO_CCEPT.NOMBRE,
  SUM(APROPIACION_CREDITO) PRESUPUESTO_INICIAL,
  SUM(APROPIACION_CREDITO+ADICION+REDUCCION +(TRASLADO_CREDITO-TRASLADO_DEBITO)) PRESUPUESTO_DEFINITIVO
FROM CUIPO_CODIGO_CCEPT
INNER JOIN PLAN_PRESUPUESTAL 
 ON CUIPO_CODIGO_CCEPT.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
AND CUIPO_CODIGO_CCEPT.ANO      = PLAN_PRESUPUESTAL.ANO
AND CUIPO_CODIGO_CCEPT.CODIGO   = PLAN_PRESUPUESTAL.CODIGO
INNER JOIN SALDO_PLAN_PPTAL
 ON PLAN_PRESUPUESTAL.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
AND PLAN_PRESUPUESTAL.ANO      = SALDO_PLAN_PPTAL.ANO
AND PLAN_PRESUPUESTAL.CODIGO   = SALDO_PLAN_PPTAL.CODIGO
INNER JOIN PLAN_CIRCULAR_UNICA   
 ON PLAN_PRESUPUESTAL.COMPANIA   = PLAN_CIRCULAR_UNICA.COMPANIA
AND PLAN_PRESUPUESTAL.CODIGO     = PLAN_CIRCULAR_UNICA.CODIGO
AND PLAN_PRESUPUESTAL.ANO        = PLAN_CIRCULAR_UNICA.ANO
WHERE CUIPO_CODIGO_CCEPT.COMPANIA = s$compania$s
AND CUIPO_CODIGO_CCEPT.ANO        = s$anio$s
AND SALDO_PLAN_PPTAL.MES        <= s$mesfinal$s
AND PLAN_PRESUPUESTAL.REGALIAS IN (0)
AND PLAN_PRESUPUESTAL.NATURALEZA IN ('C')
AND CUIPO_CODIGO_CCEPT.MOVIMIENTO NOT IN (0)
GROUP BY CUIPO_CODIGO_CCEPT.CODIGO, 
CUIPO_CODIGO_CCEPT.NOMBRE
) CUIPO
ORDER BY CODIGO