SElECT
CODIGO,
s$nombre$s
CPC,
FUENTE_FINANCIACION,
APLICA_DEST_ESPECIFICA,
TIPO_NORMA,
NUMERO_NORMA,
FECHA_NORMA,
VALOR_DESTINACION,
TERCERO,
POLITICA_PUBLICA,
SINFONDOS,
CONFONDOS,
SINFONDOS_ANT,
CONFONDOS_ANT,
SUM(SINFONDOS +
CONFONDOS +
SINFONDOS_ANT +
CONFONDOS_ANT) TOTAL 
FROM(SELECT 
CUIPO_CODIGO_CCEPT.COMPANIA,
CUIPO_CODIGO_CCEPT.ANO,
CUIPO_CODIGO_CCEPT.CODIGO,
CUIPO_CODIGO_CCEPT.NOMBRE,
NVL(DETALLE_COMPROBANTE_PPTAL.CODIGO_CPC,'0.0') CPC,
NVL(DETALLE_COMPROBANTE_PPTAL.FUENTE_CUIPO,'0.0') FUENTE_FINANCIACION,
CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN 'Si' ELSE 'No' END APLICA_DEST_ESPECIFICA,
CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN PLAN_PPTAL_CONFIG.TIPO_NORMA ELSE '0.0' END TIPO_NORMA,
CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN PLAN_PPTAL_CONFIG.NUMERO_NORMA ELSE '0.0' END NUMERO_NORMA,
CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN TO_CHAR(PLAN_PPTAL_CONFIG.FECHA_NORMA,'DD/MM/YYYY') ELSE '01/01/1900' END FECHA_NORMA,
CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN PLAN_PPTAL_CONFIG.VALOR_NORMA ELSE 0 END VALOR_DESTINACION,
NVL(TERCERO_CHIP,1) TERCERO,
NVL(PLAN_PPTAL_CONFIG.POLITCA_PUBLICA_CUIPO,'0') POLITICA_PUBLICA,
 SUM(CASE  WHEN P.CONSITUACIONFONDOS NOT IN (0)
            THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO - DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO)
            ELSE 0   
            END) AS SINFONDOS,
            
         SUM(CASE  WHEN P.CONSITUACIONFONDOS  IN (0)
              THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO - DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO)
              ELSE 0   
              END) AS CONFONDOS,
          
             SUM(CASE  WHEN PLAN_PPTAL_CONFIG.SITUACIONFONDOSSCHIP NOT IN (0)
            THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO - DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO)
            ELSE 0   
            END) AS SINFONDOS_ANT,
            
         SUM(CASE  WHEN PLAN_PPTAL_CONFIG.SITUACIONFONDOSSCHIP  IN (0)
              THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO - DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO)
              ELSE 0   
              END) AS CONFONDOS_ANT
  FROM CUIPO_CODIGO_CCEPT 
INNER JOIN DETALLE_COMPROBANTE_PPTAL
 ON CUIPO_CODIGO_CCEPT.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA
AND CUIPO_CODIGO_CCEPT.ANO      = DETALLE_COMPROBANTE_PPTAL.ANO
AND CUIPO_CODIGO_CCEPT.CODIGO   = DETALLE_COMPROBANTE_PPTAL.CUENTA
INNER JOIN TIPO_COMPROBPP
 ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA
AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
INNER JOIN PLAN_PPTAL_CONFIG
 ON CUIPO_CODIGO_CCEPT.COMPANIA = PLAN_PPTAL_CONFIG.COMPANIA
AND CUIPO_CODIGO_CCEPT.ANO      = PLAN_PPTAL_CONFIG.ANO
AND CUIPO_CODIGO_CCEPT.CODIGO   = PLAN_PPTAL_CONFIG.CODIGO
INNER JOIN PLAN_PRESUPUESTAL P 
 ON PLAN_PPTAL_CONFIG.COMPANIA = P.COMPANIA
AND PLAN_PPTAL_CONFIG.ANO      = P.ANO
AND PLAN_PPTAL_CONFIG.CODIGO   = P.CODIGO 
  WHERE P.COMPANIA = s$compania$s
    AND P.ANO =  s$anio$s
    AND DETALLE_COMPROBANTE_PPTAL.MES  <= s$mesfinal$s
    AND P.NATURALEZA ='C'
    AND P.REGALIAS IN(0)
    AND TIPO_COMPROBPP.CLASE IN ('ING','DIN','AIN')
    AND CUIPO_CODIGO_CCEPT.MOVIMIENTO NOT IN (0)
    GROUP BY CUIPO_CODIGO_CCEPT.COMPANIA, CUIPO_CODIGO_CCEPT.ANO, CUIPO_CODIGO_CCEPT.CODIGO, CUIPO_CODIGO_CCEPT.NOMBRE, NVL(DETALLE_COMPROBANTE_PPTAL.CODIGO_CPC,'0.0'), 
NVL(DETALLE_COMPROBANTE_PPTAL.FUENTE_CUIPO,'0.0'), CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN 'Si' ELSE 'No' END, CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN PLAN_PPTAL_CONFIG.TIPO_NORMA ELSE '0.0' END, CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN PLAN_PPTAL_CONFIG.NUMERO_NORMA ELSE '0.0' END, CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN TO_CHAR(PLAN_PPTAL_CONFIG.FECHA_NORMA,'DD/MM/YYYY') ELSE '01/01/1900' END, 
CASE WHEN PLAN_PPTAL_CONFIG.APLICA_DEST_ESPECIFICA NOT IN (0) THEN PLAN_PPTAL_CONFIG.VALOR_NORMA ELSE 0 END, NVL(TERCERO_CHIP,1), NVL(PLAN_PPTAL_CONFIG.POLITCA_PUBLICA_CUIPO,'0')
 ) GROUP BY CODIGO, 
 s$nombre$s
 CPC, FUENTE_FINANCIACION, APLICA_DEST_ESPECIFICA, TIPO_NORMA, 
NUMERO_NORMA, FECHA_NORMA, VALOR_DESTINACION, TERCERO, POLITICA_PUBLICA, 
SINFONDOS, CONFONDOS, SINFONDOS_ANT, CONFONDOS_ANT