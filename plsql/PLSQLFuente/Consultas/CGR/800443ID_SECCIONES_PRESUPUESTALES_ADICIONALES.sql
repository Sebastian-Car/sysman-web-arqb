SELECT CODIGO, 
s$nombre$s
SECCION_PPTAL,
NOMBRE_SECCION
FROM(
SELECT 
CUIPO_CODIGO_CCEPT.ANO,
CUIPO_CODIGO_CCEPT.CODIGO,
CUIPO_CODIGO_CCEPT.NOMBRE,
CUIPO_SECCION_PPTAL.CODIGO SECCION_PPTAL,
CUIPO_SECCION_PPTAL.NOMBRE NOMBRE_SECCION
FROM CUIPO_CODIGO_CCEPT
INNER JOIN PLAN_PPTAL_CONFIG
 ON CUIPO_CODIGO_CCEPT.COMPANIA = PLAN_PPTAL_CONFIG.COMPANIA
AND CUIPO_CODIGO_CCEPT.ANO      = PLAN_PPTAL_CONFIG.ANO
AND CUIPO_CODIGO_CCEPT.CODIGO   = PLAN_PPTAL_CONFIG.CODIGO
INNER JOIN PLAN_PRESUPUESTAL 
 ON PLAN_PPTAL_CONFIG.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
AND PLAN_PPTAL_CONFIG.ANO      = PLAN_PRESUPUESTAL.ANO
AND PLAN_PPTAL_CONFIG.CODIGO   = PLAN_PRESUPUESTAL.CODIGO 
INNER JOIN CUIPO_SECCION_PPTAL
 ON PLAN_PPTAL_CONFIG.COMPANIA            = CUIPO_SECCION_PPTAL.COMPANIA
AND PLAN_PPTAL_CONFIG.ANO                 = CUIPO_SECCION_PPTAL.ANO
AND PLAN_PPTAL_CONFIG.SECCION_PPTAL_CUIPO = CUIPO_SECCION_PPTAL.CODIGO
WHERE CUIPO_CODIGO_CCEPT.COMPANIA = s$compania$s
AND CUIPO_CODIGO_CCEPT.ANO = s$anio$s
AND CUIPO_SECCION_PPTAL.ADICIONAL NOT IN (0)
AND CUIPO_CODIGO_CCEPT.MOVIMIENTO NOT IN (0)
) CUIPO
ORDER BY CODIGO