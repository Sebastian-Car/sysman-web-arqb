SELECT VACACIONES.NUMERO_DCTO,
  VACACIONES.ID_DE_EMPLEADO,
  VACACIONES.FECHA_INICIO,
  VACACIONES.FECHA_FINAL,
  VACACIONES.DIASTOMADOS,
  VACACIONES.INICIO_DISFRUTE,
  VACACIONES.FINAL_DISFRUTE,
  NAT_TIPO_ACTO_ADTIVO.TIPOACTOADTIVO,
  VACACIONES.FECHA_ACTO,
  VACACIONES.NUMEROACTO,
  VACACIONES.OBSERVACION_V,
  NAT_CLASE_NOVEDAD_VACACIONES.NOVEDAD_VACACIONES,
  VACACIONES.DIAS,
  VACACIONES.DIASPENDIENTESDEDISFRUTE,
  VACACIONES.NUMPERIODOS,
  (PERSONAL.APELLIDO1 || ' '|| PERSONAL.APELLIDO2 || ' '|| PERSONAL.NOMBRES) NOMBRE
FROM VACACIONES
 LEFT JOIN NAT_TIPO_ACTO_ADTIVO
   ON VACACIONES.ACTO           = NAT_TIPO_ACTO_ADTIVO.CODIGO
INNER JOIN PERSONAL
   ON VACACIONES.COMPANIA       = PERSONAL.COMPANIA
  AND VACACIONES.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
 LEFT JOIN NAT_CLASE_NOVEDAD_VACACIONES
   ON VACACIONES.NOVEDAD        = NAT_CLASE_NOVEDAD_VACACIONES.CODIGO
WHERE VACACIONES.COMPANIA     = 's$compania$s'
AND VACACIONES.ID_DE_EMPLEADO = 's$empleado$s'
AND TO_CHAR(VACACIONES.FECHA_ACTO,'DD/MM/YYYY') BETWEEN s$fechainicial$s AND s$fechafinal$s 
