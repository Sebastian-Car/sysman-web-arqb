SELECT NAT_DATOS_PERSONALES.NUMERO_DCTO,
  INCAPACIDADES.INCAPACIDAD,
  INCAPACIDADES.FECHA_INICIO,
  INCAPACIDADES.FECHA_FIN,
  INCAPACIDADES.AUTO,
  INCAPACIDADES.FECHA_AUTO,
  INCAPACIDADES.ANO,
  INCAPACIDADES.MES,
  INCAPACIDADES.PERIODO,
  INCAPACIDADES.DIAS,
  INCAPACIDADES.ENTIDADQUELAEMITE,
  INCAPACIDADES.FECHAEMISION,
  INCAPACIDADES.DIAGNOSTICO,
  INCAPACIDADES.ORIGEN,
  NAT_DATOS_PERSONALES.APELLIDO1,
  NAT_DATOS_PERSONALES.APELLIDO2,
  NAT_DATOS_PERSONALES.NOMBRES,
  NAT_DATOS_PERSONALES.NUMEROCARPETA,
  PERSONAL.NOMBRECOMPLETO
FROM NAT_DATOS_PERSONALES
	INNER JOIN PERSONAL
		ON NAT_DATOS_PERSONALES.COMPANIA     = PERSONAL.COMPANIA
		AND NAT_DATOS_PERSONALES.NUMERO_DCTO = PERSONAL.NUMERO_DCTO
	INNER JOIN INCAPACIDADES
		ON INCAPACIDADES.COMPANIA           = PERSONAL.COMPANIA
		AND INCAPACIDADES.ID_DE_EMPLEADO    = PERSONAL.ID_DE_EMPLEADO
WHERE NAT_DATOS_PERSONALES.COMPANIA ='s$compania$s'
	AND NAT_DATOS_PERSONALES.NUMERO_DCTO BETWEEN 's$empleadoInicial$s' AND 's$empleadoFinal$s'
	AND INCAPACIDADES.FECHA_INICIO BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s'
,'DD/MM/YYYY')
ORDER BY NAT_DATOS_PERSONALES.NUMERO_DCTO,
  INCAPACIDADES.FECHA_INICIO,
  INCAPACIDADES.FECHA_FIN
