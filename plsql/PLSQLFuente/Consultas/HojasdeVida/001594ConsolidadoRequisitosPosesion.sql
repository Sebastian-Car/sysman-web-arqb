SELECT 
  PERSONAL.NUMEROCARPETA
 ,PERSONAL.CODIGO
 ,PERSONAL.NUMERO_DCTO
 ,(PERSONAL.APELLIDO1 ||' '||PERSONAL.APELLIDO2||' '||PERSONAL.NOMBRES) NOMBREPERSONA
 ,REQUISITOS.RP_FORMHV
 ,REQUISITOS.RP_ANTEDISI
 ,REQUISITOS.RP_ANTEJURI
 ,REQUISITOS.RP_DOCUIDEN
 ,REQUISITOS.RP_LIBRMILI
 ,REQUISITOS.RP_FOTO
 ,REQUISITOS.RP_DECLOBLIGALIM
 ,REQUISITOS.RP_EPS
 ,REQUISITOS.RP_AUTO
 ,REQUISITOS.RP_AFIL
 ,REQUISITOS.RP_TARJPROF
 ,REQUISITOS.RP_CTAAHORROS
 ,REQUISITOS.RP_INHAINCO
 ,REQUISITOS.RP_BIENRENT
 ,REQUISITOS.RP_FP
 ,REQUISITOS.RP_CS
 ,REQUISITOS.RP_CC
 ,REQUISITOS.RP_AO
 ,REQUISITOS.RP_FR
 ,REQUISITOS.RP_AG
 ,REQUISITOS.RP_DP
 ,REQUISITOS.RP_CL
 ,REQUISITOS.RP_CEP
 ,REQUISITOS.RP_BRF
FROM NAT_DATOS_PERSONALES PERSONAL
  LEFT JOIN NAT_REQUISITOS_POSESION REQUISITOS
    ON PERSONAL.COMPANIA    = REQUISITOS.COMPANIA
   AND PERSONAL.NUMERO_DCTO = REQUISITOS.DP_NUMEDOCU
   AND PERSONAL.SUCURSAL    = REQUISITOS.SUCURSAL
WHERE PERSONAL.COMPANIA = s$compania$s
  AND PERSONAL.NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s
ORDER BY PERSONAL.NUMERO_DCTO
