SELECT 
  DATOS.NUMEROCARPETA
 ,DATOS.CODIGO
 ,DATOS.NUMERO_DCTO
 ,EDUCACION.GRADO
 ,EDUCACION.TITULO                                                TITULOEBM
 ,EDUCACION.ESTABLECIMIENTO
 ,EDUCACION.FECHATERMINACION
 ,CIUDAD.NOMBRE                                                   MUNTERMINO
 ,DEPARTAMENTO.NOMBRE                                             DEPTERMINO
 ,(DATOS.APELLIDO1 ||' '|| DATOS.APELLIDO2 ||' '|| DATOS.NOMBRES) NOMBREPERSONA
FROM NAT_DATOS_PERSONALES DATOS
 INNER JOIN NAT_EDUCACION_BASICAYMEDIA EDUCACION 
    ON DATOS.COMPANIA    = EDUCACION.COMPANIA
   AND DATOS.NUMERO_DCTO = EDUCACION.NUMERO_DCTO
   AND DATOS.SUCURSAL    = EDUCACION.SUCURSAL
  LEFT JOIN CIUDAD
    ON EDUCACION.PAISTERMINO = CIUDAD.PAIS
   AND EDUCACION.DEPTERMINO  = CIUDAD.DEPARTAMENTO
   AND EDUCACION.MUNTERMINO  = CIUDAD.CODIGO
  LEFT JOIN DEPARTAMENTO 
    ON EDUCACION.PAISTERMINO = DEPARTAMENTO.PAIS
   AND EDUCACION.DEPTERMINO  = DEPARTAMENTO.CODIGO
WHERE DATOS.COMPANIA = s$compania$s
  AND DATOS.NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s
ORDER BY 
  DATOS.NUMEROCARPETA   
 ,DATOS.CODIGO
 ,DATOS.NUMERO_DCTO
