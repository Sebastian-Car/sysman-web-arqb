WITH PAISDEPTOCIUDAD AS(
SELECT 
  P.PAIS
 ,D.CODIGO DEPARTAMENTO
 ,C.CODIGO CIUDAD
 ,P.NOMBRE NOMPAIS
 ,D.NOMBRE NOMDEPTO
 ,C.NOMBRE NOMCIUDAD
FROM PAISES P
  INNER JOIN DEPARTAMENTO D
    ON P.PAIS = D.PAIS
  INNER JOIN CIUDAD C
    ON D.CODIGO = C.DEPARTAMENTO
   AND D.PAIS   = C.PAIS
)
SELECT 
  NDP.NUMERO_DCTO
 ,NDP.APELLIDO1
 ,NDP.APELLIDO2
 ,NDP.NOMBRES
 ,NVL(TD.DESCRIPCION, 'OTRO')                 TIPODOC 
 ,EXP.NOMCIUDAD                               EXPEDIDA
 ,DECODE(NDP.NACIONALIDAD, 'C', 'COLOMBIANO'
 	                     , 'D', 'DOBLE NACIONALIDAD'
	                     , 'E', 'EXTRANJERO') NACIONALIDAD
 ,EXP.NOMPAIS                                 PAISEXTRANJERO
 ,G.NOMBRE                                    SEXO
 ,NDP.CLASELIBMILITAR
 ,NDP.NUMLIBMILITAR
 ,NDP.DISTRITOMILITAR
 ,NCTO.NOMPAIS                                PAISNCTO 
 ,NCTO.NOMDEPTO                               DEPTONCTO
 ,NCTO.NOMCIUDAD                              MUNICIPIONCTO
 ,NDP.FECHANCTO
 ,RESIDE.NOMPAIS                              PAISRESIDE
 ,RESIDE.NOMDEPTO                             DEPTORESIDE
 ,RESIDE.NOMCIUDAD                            MUNICIPIORESIDE
 ,NDP.DIRECCION
 ,NDP.TELEFONOS
 ,EC.DESCRIPCION                              ESTADO_CIVIL
 ,NDP.PERSONAS
 ,NDP.RH
 ,NDP.CERTIFICADOJUDICIAL
 ,NDP.LICENCIACONDUCCION
 ,NDP.ALERGIAS
 ,NDP.CODIGO
 ,NDP.BARRIO
 ,NDP.MEDICOTRATANTE
 ,NDP.ESTRATO
 ,NDP.FECHARETIRO
 ,NDP.DIRECCIONRESIDENCIA
 ,NDP.FECHAINGRESO
 ,CASE WHEN NDP.PROPIETARIO IN(0) 
       THEN 'NO'
       ELSE 'SI'
  END                                         PROPIETARIO
 ,NDP.EMAIL
 ,NDP.TELEFONORESIDENCIA
 ,EXP.NOMDEPTO                                DPTOEXPCEDULA
 ,NDP.NUMEROCARPETA
FROM NAT_DATOS_PERSONALES NDP
LEFT JOIN TIPOS_DOCUMENTOS TD
  ON NDP.COMPANIA       = TD.COMPANIA
 AND NDP.DCTO_IDENTIDAD = TD.DCTO_IDENTIDAD
LEFT JOIN PAISDEPTOCIUDAD EXP
  ON NDP.PAISEXTRANJERO = EXP.PAIS
 AND NDP.DPTOEXPCEDULA  = EXP.DEPARTAMENTO 
 AND NDP.EXPEDIDA       = EXP.CIUDAD
LEFT JOIN PAISDEPTOCIUDAD NCTO
  ON NDP.PAISNCTO      = NCTO.PAIS
 AND NDP.DEPTONCTO     = NCTO.DEPARTAMENTO
 AND NDP.MUNICIPIONCTO = NCTO.CIUDAD
LEFT JOIN PAISDEPTOCIUDAD RESIDE
  ON NDP.PAISRESIDE      = RESIDE.PAIS
 AND NDP.DEPTORESIDE     = RESIDE.DEPARTAMENTO 
 AND NDP.MUNICIPIORESIDE = RESIDE.CIUDAD 
LEFT JOIN GENEROS G
  ON NDP.SEXO = G.CODIGO  
LEFT JOIN ESTADO_CIVIL EC
  ON NDP.COMPANIA     = EC.COMPANIA
 AND NDP.ESTADO_CIVIL = EC.ESTADO_CIVIL
WHERE NDP.COMPANIA = s$compania$s
AND NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s
