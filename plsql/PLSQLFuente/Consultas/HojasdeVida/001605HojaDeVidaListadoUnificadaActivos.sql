WITH PAISDEPTOCIUDAD AS(
SELECT 
  P.PAIS
 ,D.CODIGO DEPARTAMENTO
 ,C.CODIGO CIUDAD
 ,P.NOMBRE NOMPAIS
 ,D.NOMBRE NOMDEPTO
 ,C.NOMBRE NOMCIUDAD
FROM PAISES P
  INNER JOIN DEPARTAMENTO D
    ON P.PAIS = D.PAIS
  INNER JOIN CIUDAD C
    ON D.CODIGO = C.DEPARTAMENTO
   AND D.PAIS   = C.PAIS
)
SELECT DISTINCT
  PAISDEPTOCIUDAD.NOMPAIS   RESPAIS
 ,PAISDEPTOCIUDAD.NOMDEPTO  RESDEPT
 ,PAISDEPTOCIUDAD.NOMCIUDAD RESCIUDAD
 ,N.NO_CARGON
 ,D.DCTO_IDENTIDAD
 ,D.NUMLIBMILITAR
 ,D.FECHANCTO
 ,D.NUMERO_DCTO
 ,(D.APELLIDO1 ||' '|| D.APELLIDO2 ||' '|| D.NOMBRES) NOMBREPERSONA 
 ,D.FECHAINGRESO
 ,EC.DESCRIPCION                                      ESTADO_CIVIL
 ,G.NOMBRE                                            SEXO
 ,D.DIRECCION
FROM NAT_DATOS_PERSONALES D
  LEFT JOIN PAISDEPTOCIUDAD
    ON D.PAISRESIDE      = PAISDEPTOCIUDAD.PAIS
   AND D.DEPTORESIDE     = PAISDEPTOCIUDAD.DEPARTAMENTO
   AND D.MUNICIPIORESIDE = PAISDEPTOCIUDAD.CIUDAD
  INNER JOIN NAT_NOMBRAMIENTO N 
     ON D.COMPANIA    = N.COMPANIA
    AND D.NUMERO_DCTO = N.DP_NUMEDOCU
    AND D.SUCURSAL    = N.SUCURSAL
  LEFT JOIN ESTADO_CIVIL EC
    ON D.COMPANIA     = EC.COMPANIA
   AND D.ESTADO_CIVIL = EC.ESTADO_CIVIL  
  LEFT JOIN GENEROS G
    ON D.SEXO = G.CODIGO  
WHERE D.COMPANIA = s$compania$s
  AND D.FECHARETIRO IS NULL
  AND D.NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s
ORDER BY D.NUMERO_DCTO
