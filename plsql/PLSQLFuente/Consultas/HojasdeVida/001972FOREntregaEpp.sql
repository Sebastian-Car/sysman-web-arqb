WITH ELEMENTOSEMPLEADO AS(
SELECT
     PER.COMPANIA
    ,PER.APELLIDO1
    ,PER.APELLIDO2
    ,PER.NOMBRES NOMBRE
    ,PER.NOMBRECOMPLETO
	,EPR.CEDULA
    ,C.NOMBRE_DEL_CARGO
    ,D.NOMBRE AREA    
    ,SEP.NOMBRE NOMBREELEMENTO
    ,1 CANT
    ,TO_CHAR(EPR.FECHAENTREGA, 'DD/MM/YYYY') FECHAENTREGA
    ,EPR.OBSERVACION  
    ,PCK_SYSMAN_UTL.FC_PAR( UN_COMPANIA => EPR.COMPANIA, UN_NOMBRE => 'IDENTIFICACION ENCARGADO ENTREGA EPP', UN_MODULO => 21, UN_FECHA_PAR => SYSDATE) TERCEROENC
FROM
    ELEMENTO_PROTECCION_PERSONAL EPR 
    INNER JOIN SST_ELEMENTO_PROTECCION SEP 
         ON EPR.COMPANIA = SEP.COMPANIA 
        AND EPR.ELEMENTO = SEP.CODIGOELEMENTO 
        
    INNER JOIN PERSONAL PER
         ON EPR.COMPANIA = PER.COMPANIA 
        AND EPR.ID_DE_EMPLEADO = PER.ID_DE_EMPLEADO
    INNER JOIN CARGOS C
         ON PER.COMPANIA = C.COMPANIA
        AND PER.ID_DE_CARGO = C.ID_DE_CARGO
    INNER JOIN DEPENDENCIA D
         ON PER.COMPANIA = D.COMPANIA
        AND PER.DEPENDENCIA = D.CODIGO        
WHERE
    EPR.COMPANIA = s$compania$s 
    AND EPR.CEDULA BETWEEN s$cedulaIni$s AND s$cedulaFin$s
	AND EPR.FECHAENTREGA BETWEEN  s$fechaIni$s AND s$fechaFin$s
)
SELECT 
EP.APELLIDO1
,EP.APELLIDO2
,EP.NOMBRE
,EP.CEDULA
,EP.NOMBRECOMPLETO
,EP.NOMBRE_DEL_CARGO
,EP.AREA AREA    
,EP.NOMBREELEMENTO
,EP.CANT
,EP.FECHAENTREGA
,EP.OBSERVACION  
,T.APELLIDO1 APELLIDO1ENC
,T.APELLIDO2 APELLIDO2ENC
,T.NOMBRE1 || ' ' || T.NOMBRE2  NOMBREENC
FROM ELEMENTOSEMPLEADO EP 
    LEFT JOIN TERCERO T 
         ON EP.COMPANIA = T.COMPANIA
        AND EP.TERCEROENC = T.NIT
