WITH PAISDEPTOCIUDAD AS (
SELECT 
  P.PAIS
 ,D.CODIGO DEPARTAMENTO
 ,C.CODIGO CIUDAD
 ,P.NOMBRE NOMPAIS
 ,D.NOMBRE NOMDEPTO
 ,C.NOMBRE NOMCIUDAD
FROM PAISES P
  INNER JOIN DEPARTAMENTO D
    ON P.PAIS = D.PAIS
  INNER JOIN CIUDAD C
    ON D.CODIGO = C.DEPARTAMENTO
   AND D.PAIS   = C.PAIS
)
SELECT 
  PAISDEPTOCIUDAD.NOMPAIS   RESPAIS
 ,PAISDEPTOCIUDAD.NOMDEPTO  RESDEPT
 ,PAISDEPTOCIUDAD.NOMCIUDAD RESCIUDAD
 ,DATOS.NUMERO_DCTO
 ,DATOS.APELLIDO1
 ,DATOS.APELLIDO2
 ,DATOS.NOMBRES
 ,N.EN_FECHDESDE
 ,N.NO_ACTAPOSE
 ,N.NO_CARGOA
 ,N.NO_CARGON 
FROM NAT_DATOS_PERSONALES DATOS
  LEFT JOIN PAISDEPTOCIUDAD
    ON DATOS.PAISRESIDE      = PAISDEPTOCIUDAD.PAIS
   AND DATOS.DEPTORESIDE     = PAISDEPTOCIUDAD.DEPARTAMENTO
   AND DATOS.MUNICIPIORESIDE = PAISDEPTOCIUDAD.CIUDAD
  LEFT JOIN NAT_NOMBRAMIENTO N
    ON DATOS.COMPANIA    = N.COMPANIA 
   AND DATOS.NUMERO_DCTO = N.DP_NUMEDOCU
   AND DATOS.SUCURSAL    = N.SUCURSAL
WHERE DATOS.COMPANIA = s$compania$s
  AND DATOS.NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s
  AND DATOS.NUMEROCARPETA IS NOT NULL 
  AND DATOS.FECHARETIRO IS NULL
  AND N.ULT_CARGO NOT IN(0)
ORDER BY 
  DATOS.NUMERO_DCTO
 ,N.EN_FECHDESDE
 ,DATOS.NUMEROCARPETA
