MERGE INTO CONSULTAS FIN USING (SELECT '001552HojasDeVidaTecnica' INFORME ,TO_CLOB(q'[SELECT 
  DECODE(CLASEPT, 1, 'Prima Técnica Factor'
  	            , 2, 'Prima Técnica No Factor'
  	            , 3, 'Prima de Alta Gestión') CLASEPT,
  NAT_PRIMA_TECNICA.PT_TIPODOCUACTO,
  NAT_TIPO_ACTO_ADTIVO.TIPOACTOADTIVO,
  NVL(NAT_PRIMA_TECNICA.PT_NUME,' ') PT_NUME,
  NVL(TO_CHAR(NAT_PRIMA_TECNICA.PT_FECHRESODECR,'DD/MM/YYYY'),' ') PT_FECHRESODECR,
  NAT_PRIMA_TECNICA.PT_FECHEFEC,
  NAT_PRIMA_TECNICA.PT_FACTLIQ,
  NAT_PRIMA_TECNICA.PT_TITUUNIV,
  NAT_PRIMA_TECNICA.PT_EXEPCIONAL,
  NAT_PRIMA_TECNICA.PT_CAPAESPE,
  NAT_PRIMA_TECNICA.PT_TITUUNIVADIC,
  NAT_PRIMA_TECNICA.PT_EXPERIENCIA,
  NAT_PRIMA_TECNICA.PT_RESIHORAACUM,
  NAT_PRIMA_TECNICA.PT_CAPANOFORM,
  NAT_PRIMA_TECNICA.DP_NUMEDOCU
FROM NAT_PRIMA_TECNICA
INNER JOIN NAT_TIPO_ACTO_ADTIVO
ON NAT_PRIMA_TECNICA.PT_TIPODOCUACTO  = NAT_TIPO_ACTO_ADTIVO.CODIGO
WHERE COMPANIA    = $P!{PR_COMPANIA}
  AND DP_NUMEDOCU = '$P!{PR_DP_NUMEDOCU}'
  AND SUCURSAL    = $P!{PR_SUCURSAL}
ORDER BY DP_NUMEDOCU]') CONSULTA, 21 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);

MERGE INTO CONSULTAS_SUB FIN USING (SELECT '001551HojasDeVidaSalarios' INFORME_PADRE, '001552HojasDeVidaTecnica' INFORME_HIJO, 'PR_STRSQL_HOJASDEVIDA_TECNICA' PARAMETRO ,NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (FIN.INFORME_PADRE = INI.INFORME_PADRE AND INI.INFORME_HIJO = FIN.INFORME_HIJO)  WHEN MATCHED THEN  UPDATE SET FIN.PARAMETRO = INI.PARAMETRO , FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME_PADRE,INFORME_HIJO, PARAMETRO,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME_PADRE,INI.INFORME_HIJO, INI.PARAMETRO,INI.CREATED_BY,SYSDATE);
