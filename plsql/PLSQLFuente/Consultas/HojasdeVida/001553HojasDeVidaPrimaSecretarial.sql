MERGE INTO CONSULTAS FIN USING (SELECT '001553HojasDeVidaPrimaSecretarial' INFORME ,TO_CLOB(q'[SELECT NAT_PRIMA_SERVICIOS.DP_NUMEDOCU ,
  NAT_PRIMA_SERVICIOS.PS_TIPODOCUACTO ,
  NAT_TIPO_ACTO_ADTIVO.TIPOACTOADTIVO,
  NAT_PRIMA_SERVICIOS.PS_NUME ,
  NAT_PRIMA_SERVICIOS.PS_FECHRESODECR ,
  NAT_PRIMA_SERVICIOS.PS_TIPO,
  NAT_PRIMA_SERVICIOS.PS_VALOR ,
  NAT_PRIMA_SERVICIOS.PS_PORCENTAJE
FROM NAT_PRIMA_SERVICIOS
INNER JOIN NAT_DATOS_PERSONALES 
ON
 NAT_PRIMA_SERVICIOS.COMPANIA         =NAT_DATOS_PERSONALES.COMPANIA
AND NAT_PRIMA_SERVICIOS.DP_NUMEDOCU=NAT_DATOS_PERSONALES.NUMERO_DCTO
AND NAT_PRIMA_SERVICIOS.SUCURSAL=NAT_DATOS_PERSONALES.SUCURSAL
INNER JOIN NAT_TIPO_ACTO_ADTIVO
ON NAT_PRIMA_SERVICIOS.PS_TIPODOCUACTO  = NAT_TIPO_ACTO_ADTIVO.CODIGO
WHERE NAT_DATOS_PERSONALES.COMPANIA     =$P!{PR_COMPANIA}
AND NAT_DATOS_PERSONALES.NUMERO_DCTO    = '$P!{PR_NUMERO_DCTO}'
AND NAT_DATOS_PERSONALES.SUCURSAL       =$P!{PR_SUCURSAL}
ORDER BY NAT_DATOS_PERSONALES.NUMERO_DCTO]') CONSULTA, 21 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);

MERGE INTO CONSULTAS_SUB FIN USING (SELECT '001551HojasDeVidaSalarios' INFORME_PADRE, '001553HojasDeVidaPrimaSecretarial' INFORME_HIJO, 'PR_STRSQL_HOJASDEVIDA_PRIMASECRETARIAL' PARAMETRO ,NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (FIN.INFORME_PADRE = INI.INFORME_PADRE AND INI.INFORME_HIJO = FIN.INFORME_HIJO)  WHEN MATCHED THEN  UPDATE SET FIN.PARAMETRO = INI.PARAMETRO , FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME_PADRE,INFORME_HIJO, PARAMETRO,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME_PADRE,INI.INFORME_HIJO, INI.PARAMETRO,INI.CREATED_BY,SYSDATE);
