SELECT NAT_DATOS_PERSONALES.NUMERO_DCTO,
  VACACIONES.FECHA_INICIO,
  VACACIONES.FECHA_FINAL,
  VACACIONES.FECHAPAGO,
  VACACIONES.INICIO_DISFRUTE,
  VACACIONES.FINAL_DISFRUTE,
  VACACIONES.ACTO,
  VACACIONES.FECHA_ACTO,
  VACACIONES.NUMEROACTO,
  VACACIONES.OBSERVACION_V OBSERVACIONES,
  VACACIONES.VALOR,
  VACACIONES.NOVEDAD,
  VACACIONES.DIASDINERO,
  NAT_DATOS_PERSONALES.APELLIDO1,
  NAT_DATOS_PERSONALES.APELLIDO2,
  NAT_DATOS_PERSONALES.NOMBRES,
  NAT_DATOS_PERSONALES.NUMEROCARPETA
FROM VACACIONES
 LEFT JOIN PERSONAL
 ON VACACIONES.COMPANIA  = PERSONAL.COMPANIA
 AND VACACIONES.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
 LEFT JOIN NAT_DATOS_PERSONALES
 ON PERSONAL.COMPANIA  = NAT_DATOS_PERSONALES.COMPANIA
 AND PERSONAL.NUMERO_DCTO = NAT_DATOS_PERSONALES.NUMERO_DCTO
WHERE VACACIONES.COMPANIA = 's$compania$s'
      AND VACACIONES.ENDINERO IN (0)
      AND NAT_DATOS_PERSONALES.NUMERO_DCTO BETWEEN 's$empleadoInicial$s' AND 's$empleadoFinal$s'
