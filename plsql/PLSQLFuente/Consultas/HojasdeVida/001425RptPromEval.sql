WITH MIULTIMONOMBRAMIENTO AS (
SELECT NAT_NOMBRAMIENTO.DP_NUMEDOCU,
  (APELLIDO1 || ' ' || APELLIDO2 || ' ' || NOMBRES)  EXPR1,
  MAX(NAT_NOMBRAMIENTO.NO_FECHRESODECR) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM)  PRIMERODENO_FECHRESODECR,
  MAX(NAT_NOMBRAMIENTO.NO_CODIGOCARGO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM)  PRIMERODENO_CODIGOCARGO,
  MAX(NAT_NOMBRAMIENTO.NO_GRADOCARGO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM)  PRIMERODENO_GRADOCARGO,
  MAX(NAT_NOMBRAMIENTO.NO_TIPO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM)        PRIMERODENO_TIPO,
  MAX(FORMANOMBRAMIENTO.NOMBREFORMA)  KEEP (DENSE_RANK FIRST ORDER BY ROWNUM)  PRIMERODETN_TIPO,
  MAX(NAT_NOMBRAMIENTO.NO_CARGON) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM)      PRIMERODENO_CARGON,
  MAX(NAT_NOMBRAMIENTO.NO_DEPENUEV) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM)     NO_DEPENUEV,
  MAX(NAT_NOMBRAMIENTO.NO_FECHAEFECTIVIDAD) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) PRIMERODENO_FECHAEFECTIVIDAD,
  NAT_DATOS_PERSONALES.CODIGO
FROM NAT_DATOS_PERSONALES
LEFT JOIN NAT_NOMBRAMIENTO
ON NAT_DATOS_PERSONALES.COMPANIA = NAT_NOMBRAMIENTO.COMPANIA
AND NAT_DATOS_PERSONALES.NUMERO_DCTO = NAT_NOMBRAMIENTO.DP_NUMEDOCU
AND NAT_DATOS_PERSONALES.SUCURSAL = NAT_NOMBRAMIENTO.SUCURSAL
LEFT JOIN FORMANOMBRAMIENTO
ON NAT_NOMBRAMIENTO.COMPANIA = FORMANOMBRAMIENTO.COMPANIA
AND NAT_NOMBRAMIENTO.NO_TIPO = FORMANOMBRAMIENTO.IDFORMA
WHERE NAT_NOMBRAMIENTO.COMPANIA = 's$compania$s'
AND NAT_NOMBRAMIENTO.NO_TIPO = 'CA'
GROUP BY NAT_NOMBRAMIENTO.DP_NUMEDOCU,
  (APELLIDO1 || ' ' || APELLIDO2 || ' ' || NOMBRES),
  NAT_DATOS_PERSONALES.CODIGO
ORDER BY  MAX(NAT_NOMBRAMIENTO.NO_FECHRESODECR) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) DESC
)
SELECT 
  NAT_EVALUACION.EV_FECHDESDEEVAL,
  NAT_EVALUACION.EV_FECHHASTAEVAL,
  MIULTIMONOMBRAMIENTO.PRIMERODENO_CARGON,
  MIULTIMONOMBRAMIENTO.NO_DEPENUEV,
  NAT_DATOS_PERSONALES.NUMERO_DCTO,
 NAT_DATOS_PERSONALES.COMPANIA,
  (APELLIDO1 || ' ' || APELLIDO2 || ' ' || NOMBRES) NOMBRE,
  NAT_EVALUACION.EV_FORMATO
FROM NAT_EVALUACION
LEFT JOIN NAT_DATOS_PERSONALES
ON NAT_DATOS_PERSONALES.NUMERO_DCTO   = NAT_EVALUACION.DP_NUMEDOCU
LEFT JOIN MIULTIMONOMBRAMIENTO
ON MIULTIMONOMBRAMIENTO.DP_NUMEDOCU   = NAT_DATOS_PERSONALES.NUMERO_DCTO
WHERE NAT_EVALUACION.COMPANIA =  's$compania$s'
AND NAT_EVALUACION.EV_FECHDESDEEVAL >=  s$fechaInicial$s
AND NAT_EVALUACION.EV_FECHHASTAEVAL   <=  s$fechaFinal$s
AND NAT_DATOS_PERSONALES.NUMERO_DCTO BETWEEN  's$cedulaInicial$s' AND  's$cedulaFinal$s'
