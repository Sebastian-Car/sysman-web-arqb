SELECT 
  DATOS.NUMEROCARPETA
 ,DATOS.CODIGO
 ,DATOS.NUMERO_DCTO
 ,DATOS.APELLIDO1 ||' '|| DATOS.APELLIDO2 ||' '|| DATOS.NOMBRES NOMBREPERSONA
 ,P.PU_CONSECUTIVO
 ,P.PU_DESCRIPCION
FROM NAT_DATOS_PERSONALES DATOS
  LEFT JOIN NAT_PUBLICACIONES P
    ON DATOS.COMPANIA    = P.COMPANIA
   AND DATOS.NUMERO_DCTO = P.DP_NUMEDOCU
   AND DATOS.SUCURSAL    = P.SUCURSAL
WHERE DATOS.COMPANIA = s$compania$s
  AND DATOS.NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s
  AND P.PU_DESCRIPCION IS NOT NULL
