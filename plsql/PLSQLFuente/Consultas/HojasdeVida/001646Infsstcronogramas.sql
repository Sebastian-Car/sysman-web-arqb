SELECT SST_CRONOGRAMA.RESPONSABLE,
  SST_CRONOGRAMA.COMPANIA,
  SST_CRONOGRAMA.CONSECUTIVO,
  UPPER(SST_TIPO_ACTIVIDAD.NOMBRE) AS NOMBRECRONO,
  SST_CRONOGRAMA.FECHA_TRANSACCION,
  SST_CRONOGRAMA.FECHA_INICIAL,
  SST_CRONOGRAMA.FECHA_FINAL,
  SST_CRONOGRAMA.HORA_INICIAL,
  SST_CRONOGRAMA.HORA_FINAL,
  TERCERO.NOMBRE NOMBRESS,
  SST_CRONOGRAMA.SUCURSAL,
  SST_CRONOGRAMA.DURACION,
  SST_CRONOGRAMA.DESCRIPCION,
  SST_CRONOGRAMA.COSTO_ESTIMADO,
  SST_CRONOGRAMA.FECHA_EJECUCION,
  SST_CRONOGRAMA.CODIGO_ACTIVIDAD
FROM SST_CRONOGRAMA
INNER JOIN SST_TIPO_ACTIVIDAD
ON SST_CRONOGRAMA.COMPANIA          = SST_TIPO_ACTIVIDAD.COMPANIA
AND SST_CRONOGRAMA.CODIGO_ACTIVIDAD = SST_TIPO_ACTIVIDAD.CODIGO
AND SST_CRONOGRAMA.TIPO_TRANSACCION = SST_TIPO_ACTIVIDAD.TIPO_TRANSACCION
INNER JOIN TERCERO
ON SST_CRONOGRAMA.COMPANIA     = TERCERO.COMPANIA
AND SST_CRONOGRAMA.RESPONSABLE = TERCERO.NIT
AND SST_CRONOGRAMA.SUCURSAL    = TERCERO.SUCURSAL
WHERE SST_CRONOGRAMA.COMPANIA  = s$compania$s
s$condicion$s 

AND SST_CRONOGRAMA.FECHA_TRANSACCION BETWEEN s$fechaInicial$s AND s$fechaFinal$s
AND SST_CRONOGRAMA.TIPO_TRANSACCION =  s$tipoTransaccion$s
