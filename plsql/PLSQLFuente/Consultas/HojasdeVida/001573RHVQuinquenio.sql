SELECT
 
NAT_QUINQUENIO.QU_TIPODOCUACTO,
NAT_QUINQUENIO.QU_NUME,
NAT_QUINQUENIO.QU_FECHRESO,
NAT_QUINQUENIO.QU_FECHDESDE,
NAT_QUINQUENIO.QU_FECHHASTA,
NAT_QUINQUENIO.QU_VALOR,
NAT_QUINQUENIO.DP_NUMEDOCU,
  NAT_DATOS_PERSONALES.APELLIDO1|| ' '|| NAT_DATOS_PERSONALES.APELLIDO2|| ' '|| NAT_DATOS_PERSONALES.NOMBRES    NOMBREEMPLEADO,
  NAT_DATOS_PERSONALES.NUMEROCARPETA
FROM NAT_QUINQUENIO
LEFT JOIN NAT_DATOS_PERSONALES
ON NAT_QUINQUENIO.DP_NUMEDOCU=NAT_DATOS_PERSONALES.NUMERO_DCTO
AND NAT_QUINQUENIO.COMPANIA=NAT_DATOS_PERSONALES.COMPANIA
WHERE NAT_QUINQUENIO.COMPANIA = s$compania$s 
AND  NAT_DATOS_PERSONALES.NUMEROCARPETA  BETWEEN 's$carpetaInicial$s' AND 's$carpetaFinal$s'
