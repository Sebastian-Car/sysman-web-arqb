SELECT PERSONAL.COMPANIA,
  PERSONAL.FONDO_SALUD,
  CASE
    WHEN PERSONAL.NUMERO_DCTO ='01'
    THEN 'C'
    WHEN PERSONAL.NUMERO_DCTO='02'
    THEN 'E'
    WHEN PERSONAL.NUMERO_DCTO='03'
    THEN 'P'
    WHEN PERSONAL.NUMERO_DCTO='04'
    THEN 'T'
    WHEN PERSONAL.NUMERO_DCTO='05'
    THEN 'N'
    ELSE 'O'
  END                  AS DCTO_IDENTIDADE,
  PERSONAL.NUMERO_DCTO AS NUMERO_DCTOE,
  PERSONAL.NOMBRES     AS NOMBRESE,
  PERSONAL.APELLIDO1   AS APELLIDO1E,
  PERSONAL.APELLIDO2   AS APELLIDO2E,
  PERSONAL.DIRECCION   AS DIRECCIONE,
  PERSONAL.EXPEDIDA AS EXPEDIDAE,
  CASE
    WHEN FAMILIARES.DCTO_IDENTIDAD='01'
    THEN 'C'
    WHEN FAMILIARES.DCTO_IDENTIDAD='02'
    THEN 'E'
    WHEN FAMILIARES.DCTO_IDENTIDAD='03'
    THEN 'P'
    WHEN FAMILIARES.DCTO_IDENTIDAD='04'
    THEN 'T'
    WHEN FAMILIARES.DCTO_IDENTIDAD='05'
    THEN 'N'
    ELSE 'O'
  END                       AS DCTO_IDENTIDADB,
  FAMILIARES.IDENTIFICACION AS NUMERO_DCTOB,
  FAMILIARES.NOMBRE         AS NOMBREB,
  FAMILIARES.APELLIDO1      AS APELLIDO1B,
  FAMILIARES.APELLIDO2      AS APELLIDO2B,
  FAMILIARES.SEXO           AS SEXOB,
  FAMILIARES.FECHANCTO      AS FECHANCTOB,
  FAMILIARES.DIRECCION      AS DIRECCIONB,
  FAMILIARES.ESTADO_ACTUAL  AS ESTADO_ACTUALB,
  CASE
    WHEN FAMILIARES.PARENTESCO='02'
    THEN '1'
    WHEN FAMILIARES.PARENTESCO='03'
    THEN '2'
    WHEN FAMILIARES.PARENTESCO='04'
    THEN '3'
    WHEN FAMILIARES.PARENTESCO='05'
    THEN 'H'
    WHEN FAMILIARES.PARENTESCO='08'
    THEN '4'
    WHEN FAMILIARES.PARENTESCO='09'
    THEN '5'
    WHEN FAMILIARES.PARENTESCO='10'
    THEN '6'
    WHEN FAMILIARES.PARENTESCO='11'
    THEN '7'
    ELSE NULL
  END AS PARENTESCOB
FROM PERSONAL
INNER JOIN FAMILIARES
ON PERSONAL.COMPANIA        = FAMILIARES.COMPANIA
AND PERSONAL.ID_DE_EMPLEADO = FAMILIARES.ID_DE_EMPLEADO
WHERE PERSONAL.COMPANIA     ='s$compania$s'
AND PERSONAL.FONDO_SALUD   ='s$fondoSalud$s'
