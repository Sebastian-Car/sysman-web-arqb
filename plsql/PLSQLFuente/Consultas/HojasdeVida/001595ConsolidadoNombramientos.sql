SELECT 
  PERSONAL.NUMEROCARPETA
 ,PERSONAL.CODIGO
 ,PERSONAL.NUMERO_DCTO
 ,(PERSONAL.APELLIDO1 ||' '|| PERSONAL.APELLIDO2 ||' '|| PERSONAL.NOMBRES) NOMBREPERSONA
 ,NOMBRAMIENTO.NO_TIPODOCUACTO
 ,NOMBRAMIENTO.NO_NUME
 ,NOMBRAMIENTO.NO_FECHRESODECR
 ,NOMBRAMIENTO.NO_CARGOA
 ,NOMBRAMIENTO.NO_SALARIOA
 ,NOMBRAMIENTO.NO_CODIGOCARGO
 ,NOMBRAMIENTO.NO_GRADOCARGO
 ,NOMBRAMIENTO.NO_FECHACARGO
 ,NOMBRAMIENTO.NO_CARGON
 ,NOMBRAMIENTO.NO_SALARION
 ,NOMBRAMIENTO.NO_ACTAPOSE
 ,NOMBRAMIENTO.NO_FECHACTAPOSE
 ,NOMBRAMIENTO.NO_TIPO
 ,NOMBRAMIENTO.NO_CAUSA
 ,NOMBRAMIENTO.NO_DEPEANTE
 ,NOMBRAMIENTO.NO_DEPENUEV
 ,NOMBRAMIENTO.EN_FECHDESDE
 ,NOMBRAMIENTO.EN_FECHHASTA
 ,NOMBRAMIENTO.NO_FECHAEFECTIVIDAD
 ,NOMBRAMIENTO.EVALUADOPRUEBA
 ,NOMBRAMIENTO.PRIMATECNICAFACTOR
 ,NOMBRAMIENTO.PRIMATECNICANFACTOR
 ,NOMBRAMIENTO.PRIMAALTAGESTION
 ,NOMBRAMIENTO.GASTOSREPRESENTACION
 ,TIPO.NOMBREFORMA                                                         TN_NOMBRE
 ,NOMBRAMIENTO.NO_AUXT
 ,NOMBRAMIENTO.NO_AUXA
FROM NAT_DATOS_PERSONALES PERSONAL
  LEFT JOIN NAT_NOMBRAMIENTO NOMBRAMIENTO
    ON PERSONAL.COMPANIA    = NOMBRAMIENTO.COMPANIA
   AND PERSONAL.NUMERO_DCTO = NOMBRAMIENTO.DP_NUMEDOCU
   AND PERSONAL.SUCURSAL    = NOMBRAMIENTO.SUCURSAL
  LEFT JOIN FORMANOMBRAMIENTO TIPO
    ON NOMBRAMIENTO.COMPANIA = TIPO.COMPANIA
   AND NOMBRAMIENTO.NO_TIPO  = TIPO.IDFORMA
WHERE PERSONAL.COMPANIA = s$compania$s
  AND PERSONAL.NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s
  AND NOMBRAMIENTO.RETIRO_O_NOMBRAMIENTO IN('N')
