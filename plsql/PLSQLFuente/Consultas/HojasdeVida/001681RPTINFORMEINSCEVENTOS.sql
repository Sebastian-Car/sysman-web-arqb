SELECT 
	AI.IDEVENTO, 
	CASE AP.TIPOEVENTO
		WHEN 1 THEN 'Salud'
		WHEN 2 THEN 'Deportivo'
		WHEN 3 THEN 'Cultural'
	ELSE 'Recreativo' 
	END TIPODEEVENTO,
	AP.NOMBREEVENTO, 
	AP.NUMEROHORAS, 
	AP.FECHAFINAL, 
	AI.FECHAINICIAL, 
	(NVL(TE.NOMBRE1,' ') || ' ' || NVL(TE.NOMBRE2,' ') || ' ' || NVL(TE.APELLIDO1,' ') ||  ' ' || NVL(TE.APELLIDO2,' ')) COORDEVENTO,
	AI.NUMERO_DCTO,  
	(NVL(DP.NOMBRES,' ') || ' ' || NVL(DP.APELLIDO1,' ') ||  ' ' || NVL(DP.APELLIDO2,' ')) NOMBRE,
  CASE WHEN DP.SEXO = 'F' 
  		 THEN 'Femenino'
		 ELSE 'Masculino'
	END GENERO,
	CASE WHEN AI.BENEFICIARIO IN (0) THEN ' ' ELSE 'X' END BENEFICIARIO,
	AI.CALIFICACION,
	CASE WHEN AI.ASISTIO IN (0) THEN ' ' ELSE 'X' END ASISTIO, 
	(PCK_SYSMAN_UTL.FC_EDAD(UN_FECHA => DP.FECHANCTO)) EDAD,
	CASE AP.ESTADO
		WHEN 'P' THEN 'Programado'
		WHEN 'E' THEN 'Ejecutado'
		WHEN 'C' THEN 'Cancelado'
	ELSE ' ' 
	END ESTADOEVENTO,
	CA.NITESTABLECIMIENTO NITORGANIZADOR 
FROM NAT_ACTIVIDADESPROGRAMADAS AP 
INNER JOIN NAT_ACTIVIDADESINSCRITOS AI 
	ON AP.COMPANIA 		= AI.COMPANIA 
	AND AP.IDEVENTO 	= AI.IDEVENTO 
	AND AP.TIPOEVENTO 	= AI.TIPOEVENTO
	AND AP.FECHAINICIAL = AI.FECHAINICIAL 
INNER JOIN NAT_DATOS_PERSONALES  DP 
	ON AI.COMPANIA 		= DP.COMPANIA 
	AND AI.NUMERO_DCTO  = DP.NUMERO_DCTO 
LEFT JOIN TERCERO TE 
	ON TE.COMPANIA  = AP.COMPANIA 
	AND TE.NIT 		= AP.COORDEVENTO 
  AND TE.SUCURSAL   = AP.SUCURSALCOOR
LEFT JOIN NAT_COTIZACIONESACTIVIDADES CA 
	ON AP.COMPANIA      		= CA.COMPANIA 
	AND AP.IDEVENTO 			= CA.IDEVENTO 
	AND AP.TIPOEVENTO 			= CA.TIPOEVENTO 
	AND AP.FECHAINICIAL 		= CA.FECHAINICIAL 
	AND AP.NITESTABLECIMIENTO   = CA.NITESTABLECIMIENTO
  WHERE AP.COMPANIA = s$compania$s 
  AND (AI.BENEFICIARIO NOT IN(0) AND  AI.BENEFICIARIO  IS  NOT NULL)
	s$cadenaTipoEvento$s
	s$cadenaFechaEvento$s
	s$cadenaAsistente$s 
UNION  
