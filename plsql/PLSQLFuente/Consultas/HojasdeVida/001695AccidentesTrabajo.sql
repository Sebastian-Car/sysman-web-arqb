SELECT 
    PE.NOMBRECOMPLETO ,
    PE.NUMERO_DCTO,
    PE.ID_DE_EMPLEADO,
    PE.SEXO,
    PE.FECHA_DE_INGRESO, 
    PE.COMPANIA, 
    CARGOS.NOMBRE_DEL_CARGO NO_CARGON,
    DEP.NOMBRE NOMBREDEPENDENCIA,
    SI.FACTOR_RIESGO NOMBREFACTORRIESGO,
    SI.CONCEPTO_RIESGO,
    SI.CRITERIO_RIESGO,
    SI.NATURALEZA_LESION,
    SI.PARTE_CUERPO_AFEC,
    SI.AGENTE_LESION,
    SI.TIPO_ACCIDENTE,
    SI.CAUSA_ACCIDENTE,
    SI.ACCIONES_CORRECT
FROM NAT_SEGURIDAD_INDUSTRIAL SI
INNER JOIN PERSONAL PE 
	ON PE.COMPANIA = SI.COMPANIA
	AND PE.ID_DE_EMPLEADO = SI.ID_DE_EMPLEADO
INNER JOIN DEPENDENCIA DEP 
	ON DEP.COMPANIA = PE.COMPANIA
	AND DEP.CODIGO = PE.DEPENDENCIA
LEFT JOIN CARGOS 
	ON CARGOS.COMPANIA = PE.COMPANIA 
	AND CARGOS.ID_DE_CARGO = PE.ID_DE_CARGO 
WHERE SI.COMPANIA       = s$compania$s
  AND PE.NUMERO_DCTO = 's$noDocumentoEmpleado$s'	
