SELECT 
  NAT_APERTURA.NRO_CONVOCATORIA,
  NAT_APERTURA_INSCRITOS.NUMERO_DCTO,
  NAT_APERTURA_INSCRITOS.FECHA_RECIBO,
  NAT_APERTURA_INSCRITOS.FOLIOS,
  NAT_APERTURA_INSCRITOS.NRO_FORMULARIO,
  NAT_APERTURA_INSCRITOS.OBSERVACIONES,
  NAT_APERTURA.FECHA_CONVOCATORIA,
  NAT_APERTURA_INSCRITOS.NOMBRE,
  NAT_APERTURA_INSCRITOS.TELEFONO TELEFONOS,
  NAT_APERTURA_INSCRITOS.DIRECCION,
  NAT_APERTURA.CLASE,
  NAT_APERTURA.PLAZAS,
  NAT_APERTURA.LUGAR_INSCRIPCION,
  NAT_APERTURA.HORA_ATENCION,
  NAT_APERTURA.FECHA_CONCURSO,
  NAT_APERTURA.LUGAR_CONCURSO,
  NAT_APERTURA.HORA_CONCURSO,
  NAT_APERTURA.PUBLICACION,
  NAT_APERTURA.SUELDO,
  NAT_APERTURA.FECHAINICIOINSCRIPCION,
  NAT_APERTURA.FECHACIERREINSCRIPCION,
  NAT_APERTURA.UBICACIONORGANICA,
  NAT_APERTURA.LUGARTRABAJO,
  CARGOS.NOMBRE_DEL_CARGO  DENOMINACION,
  NAT_APERTURA.CODIGOCARGO,
  NAT_APERTURA.GRADOCARGO,
  NAT_RELACION_PRUEBAS.CONSECUTIVO,
  NAT_RELACION_PRUEBAS.CALIFICACION APROBATORIA,
  NAT_CALIFICACION_PRUEBAS.CALIFICACION,
  NAT_CALIFICACION_PRUEBAS.PRUEBA,
  NAT_RELACION_PRUEBAS.PUNMAX
FROM CARGOS
INNER JOIN NAT_APERTURA
ON CARGOS.COMPANIA = NAT_APERTURA.COMPANIA
AND CARGOS.ID_DE_CARGO  = NAT_APERTURA.CODIGOCARGO
AND CARGOS.GRADO   = NAT_APERTURA.GRADOCARGO
INNER JOIN NAT_RELACION_PRUEBAS
ON NAT_APERTURA.COMPANIA          = NAT_RELACION_PRUEBAS.COMPANIA
AND NAT_APERTURA.NRO_CONVOCATORIA = NAT_RELACION_PRUEBAS.NRO_CONVOCATORIA
INNER JOIN NAT_CALIFICACION_PRUEBAS
ON NAT_RELACION_PRUEBAS.COMPANIA          = NAT_CALIFICACION_PRUEBAS.COMPANIA
AND NAT_RELACION_PRUEBAS.NRO_CONVOCATORIA = NAT_CALIFICACION_PRUEBAS.NRO_CONVOCATORIA
AND NAT_RELACION_PRUEBAS.PRUEBA           = NAT_CALIFICACION_PRUEBAS.PRUEBA
INNER JOIN NAT_APERTURA_INSCRITOS
ON NAT_CALIFICACION_PRUEBAS.COMPANIA          = NAT_APERTURA_INSCRITOS.COMPANIA
AND NAT_CALIFICACION_PRUEBAS.NUMERO_DCTO      = NAT_APERTURA_INSCRITOS.NUMERO_DCTO
AND NAT_CALIFICACION_PRUEBAS.SUCURSAL         = NAT_APERTURA_INSCRITOS.SUCURSAL
AND NAT_CALIFICACION_PRUEBAS.NRO_CONVOCATORIA = NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA
WHERE NAT_APERTURA_INSCRITOS.COMPANIA       = s$compania$s
AND NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA = s$convocatoria$s
AND NAT_RELACION_PRUEBAS.PRUEBA             = s$prueba$s
AND NAT_CALIFICACION_PRUEBAS.CALIFICACION   < NAT_RELACION_PRUEBAS.CALIFICACION
