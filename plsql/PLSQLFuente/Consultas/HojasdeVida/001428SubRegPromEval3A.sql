WITH EVAL_CONTOTAL AS (
  SELECT SUM(ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL) - TRUNC(EV_FECHHASTAEVAL)))) SUMADIAS,
         EVALUACION.DP_NUMEDOCU,
         EVALUACION.EV_CODIGOPERSONA
  FROM NAT_EVALUACION EVALUACION
  WHERE EVALUACION.EV_FECHDESDEEVAL > $P{PR_FECHAINICIAL}
  AND EVALUACION.EV_FECHHASTAEVAL   <    $P{PR_FECHAFINAL} 
  GROUP BY EVALUACION.DP_NUMEDOCU,
    EVALUACION.EV_CODIGOPERSONA
)
SELECT 
EVALUACION.DP_NUMEDOCU, 
EVALUACION.EV_P_PLAN,
EVALUACION.EV_P_UTILRECU,
EVALUACION.EV_P_CALI,
EVALUACION.EV_P_COMPTECN,
EVALUACION.EV_P_RESP,
EVALUACION.EV_AP_LIDE,
EVALUACION.EV_AP_TOMADECI,
EVALUACION.EV_AP_SUPE,
EVALUACION.EV_AP_DELE,
EVALUACION.EV_AP_TRABEQUI,
EVALUACION.EV_CL_COMPINST,
EVALUACION.EV_CL_RELAINTE,
EVALUACION.EV_CL_INIC,
EVALUACION.EV_CL_TRATINFO,
EVALUACION.EV_FORMATO, 
EVAL_CONTOTAL.SUMADIAS, 
((EVALUACION.EV_P_PLAN)*(ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL)-TRUNC(EV_FECHHASTAEVAL)))-NVL(DIASNOEVALUADOS,0)))/EVAL_CONTOTAL.SUMADIAS EV_P_PLAN2,
((EVALUACION.EV_P_UTILRECU)*(ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL)-TRUNC(EV_FECHHASTAEVAL)))-NVL(DIASNOEVALUADOS,0)))/EVAL_CONTOTAL.SUMADIAS EV_P_UTILRECU2,
((EVALUACION.EV_P_CALI)*(ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL)-TRUNC(EV_FECHHASTAEVAL)))-NVL(DIASNOEVALUADOS,0)))/EVAL_CONTOTAL.SUMADIAS EV_P_CALI2,
((EVALUACION.EV_P_COMPTECN)*(ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL)-TRUNC(EV_FECHHASTAEVAL)))-NVL(DIASNOEVALUADOS,0)))/EVAL_CONTOTAL.SUMADIAS EV_P_COMPTECN2,
((EVALUACION.EV_P_RESP)*(ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL)-TRUNC(EV_FECHHASTAEVAL)))-NVL(DIASNOEVALUADOS,0)))/EVAL_CONTOTAL.SUMADIAS EV_P_RESP2,
((EVALUACION.EV_AP_LIDE)*(ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL)-TRUNC(EV_FECHHASTAEVAL)))-NVL(DIASNOEVALUADOS,0)))/EVAL_CONTOTAL.SUMADIAS EV_AP_LIDE2,
((EVALUACION.EV_AP_TOMADECI)*(ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL)-TRUNC(EV_FECHHASTAEVAL)))-NVL(DIASNOEVALUADOS,0)))/EVAL_CONTOTAL.SUMADIAS EV_AP_TOMADECI2,
((EVALUACION.EV_AP_SUPE)*(ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL)-TRUNC(EV_FECHHASTAEVAL)))-NVL(DIASNOEVALUADOS,0)))/EVAL_CONTOTAL.SUMADIAS EV_AP_SUPE2,
((EVALUACION.EV_AP_DELE)*(ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL)-TRUNC(EV_FECHHASTAEVAL)))-NVL(DIASNOEVALUADOS,0)))/EVAL_CONTOTAL.SUMADIAS EV_AP_DELE2,
((EVALUACION.EV_AP_TRABEQUI)*(ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL)-TRUNC(EV_FECHHASTAEVAL)))-NVL(DIASNOEVALUADOS,0)))/EVAL_CONTOTAL.SUMADIAS EV_AP_TRABEQUI2,
((EVALUACION.EV_CL_COMPINST)*(ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL)-TRUNC(EV_FECHHASTAEVAL)))-NVL(DIASNOEVALUADOS,0)))/EVAL_CONTOTAL.SUMADIAS EV_CL_COMPINST2,
((EVALUACION.EV_CL_RELAINTE)*(ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL)-TRUNC(EV_FECHHASTAEVAL)))-NVL(DIASNOEVALUADOS,0)))/EVAL_CONTOTAL.SUMADIAS EV_CL_RELAINTE2,
((EVALUACION.EV_CL_INIC)*(ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL)-TRUNC(EV_FECHHASTAEVAL)))-NVL(DIASNOEVALUADOS,0)))/EVAL_CONTOTAL.SUMADIAS EV_CL_INIC2,
((EVALUACION.EV_CL_TRATINFO)*(ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL)-TRUNC(EV_FECHHASTAEVAL)))-NVL(DIASNOEVALUADOS,0)))/EVAL_CONTOTAL.SUMADIAS EV_CL_TRATINFO2,
((EVALUACION.EV_PUNTOBJEDESE)*(ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL)-TRUNC(EV_FECHHASTAEVAL)))-NVL(DIASNOEVALUADOS,0)))/EVAL_CONTOTAL.SUMADIAS PUNOBT2,
ABS(TO_NUMBER(TRUNC(EV_FECHDESDEEVAL)-TRUNC(EV_FECHHASTAEVAL)))-NVL(DIASNOEVALUADOS,0) TOTALDIAS, 
EVALUACION.EV_PUNTOBJEDESE AS PUNOBT, 
EVALUACION.EV_FECHDESDEEVAL, 
EVALUACION.EV_FECHHASTAEVAL
FROM NAT_EVALUACION EVALUACION 
LEFT JOIN EVAL_CONTOTAL 
ON EVALUACION.DP_NUMEDOCU = EVAL_CONTOTAL.DP_NUMEDOCU
WHERE EVALUACION.COMPANIA = $P{PR_COMPANIA} 
AND EVALUACION.DP_NUMEDOCU =  $P{PR_DP_NUMEDOCU}
AND EVALUACION.EV_FORMATO='A' 
AND EVALUACION.CONSECUTIVO = 3
AND EVALUACION.EV_FECHDESDEEVAL>= $P{PR_FECHAINICIAL} 
AND EVALUACION.EV_FECHHASTAEVAL<= $P{PR_FECHAFINAL}
