SELECT TO_CHAR(NAT_RELACION_DIARIOS.FECHA,'DD/MM/YYYY') FECHA,
  NAT_DIARIOS.NOMBRE DIARIO,
  NAT_RELACION_DIARIOS.SECCION
FROM NAT_RELACION_DIARIOS
INNER JOIN NAT_DIARIOS
ON NAT_RELACION_DIARIOS.COMPANIA = NAT_DIARIOS.COMPANIA
AND NAT_RELACION_DIARIOS.DIARIO = NAT_DIARIOS.DIARIO
WHERE NAT_RELACION_DIARIOS.COMPANIA        = '$P!{PR_COMPANIA}' 
AND NAT_RELACION_DIARIOS.NRO_CONVOCATORIA  = $P!{PR_NRO_CONVOCATORIA}
