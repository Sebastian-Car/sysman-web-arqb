SELECT 
  PERSONAL.NUMEROCARPETA
 ,PERSONAL.CODIGO
 ,PERSONAL.NUMERO_DCTO
 ,(PERSONAL.APELLIDO1 ||' '|| PERSONAL.APELLIDO2 ||' '|| PERSONAL.NOMBRES) NOMBREPERSONA
 ,IDIOMA.NUMERO
 ,IDIOMA.NOMBRE
 ,IDIOMA.HABLA
 ,IDIOMA.LEE
 ,IDIOMA.ESCRIBE
 ,IDIOMA.NOTAS
FROM NAT_DATOS_PERSONALES PERSONAL
  LEFT JOIN NAT_IDIOMAS IDIOMA
    ON PERSONAL.COMPANIA    = IDIOMA.COMPANIA
   AND PERSONAL.NUMERO_DCTO = IDIOMA.NUMERO_DCTO
   AND PERSONAL.SUCURSAL    = IDIOMA.SUCURSAL
WHERE PERSONAL.COMPANIA = s$compania$s
  AND PERSONAL.NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s
  AND IDIOMA.NOMBRE IS NOT NULL
