SELECT CODIGO,
  NUMERO_DCTO,
  NVL(APELLIDO1, ' ') APELLIDO1,
  NVL(APELLIDO2, ' ') APELLIDO2,
  NVL(NOMBRES, ' ') NOMBRES,
  NVL(TO_CHAR(FECHAINGRESO, 'DD/MM/YYYY'), ' ') FECHAINGRESO
FROM NAT_DATOS_PERSONALES
WHERE COMPANIA = s$compania$s
  AND NAT_DATOS_PERSONALES.NUMEROCARPETA IS NULL
ORDER BY NAT_DATOS_PERSONALES.FECHAINGRESO DESC
