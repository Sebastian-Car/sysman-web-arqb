WITH PAISDEPTOCIUDAD AS(
SELECT 
  P.PAIS
 ,D.CODIGO DEPARTAMENTO
 ,C.CODIGO CIUDAD
 ,P.NOMBRE NOMPAIS
 ,D.NOMBRE NOMDEPTO
 ,C.NOMBRE NOMCIUDAD
FROM PAISES P
  INNER JOIN DEPARTAMENTO D
    ON P.PAIS = D.PAIS
  INNER JOIN CIUDAD C
    ON D.CODIGO = C.DEPARTAMENTO
   AND D.PAIS   = C.PAIS
)
SELECT 
  FIRST_VALUE(DATOS.NUMERO_DCTO) OVER (ORDER BY DATOS.NUMERO_DCTO) PRIMERODENUMERO_DCTO
 ,(DATOS.APELLIDO1 ||' '|| DATOS.APELLIDO2 ||' '|| DATOS.NOMBRES)  NOMBREPERSONA
 ,DATOS.FECHANCTO
 ,RESIDE.NOMDEPTO                                                  RESDEPT
 ,RESIDE.NOMCIUDAD                                                 MUNICIPIORESIDE
 ,DATOS.NUMLIBMILITAR
 ,DATOS.DIRECCION
 ,DATOS.RH
 ,EC.DESCRIPCION                                                   ESTADO_CIVIL
 ,NCTO.NOMCIUDAD                                                   MUNICIPIONCTO
 ,NCTO.NOMDEPTO                                                    DEPTONCTO
 ,DATOS.NUMERO_DCTO
FROM NAT_DATOS_PERSONALES DATOS
  LEFT JOIN PAISDEPTOCIUDAD NCTO
	ON DATOS.PAISNCTO      = NCTO.PAIS
   AND DATOS.DEPTONCTO     = NCTO.DEPARTAMENTO
   AND DATOS.MUNICIPIONCTO = NCTO.CIUDAD
  LEFT JOIN PAISDEPTOCIUDAD RESIDE
    ON DATOS.PAISRESIDE      = RESIDE.PAIS
   AND DATOS.DEPTORESIDE     = RESIDE.DEPARTAMENTO
   AND DATOS.MUNICIPIORESIDE = RESIDE.CIUDAD  
  LEFT JOIN NAT_NOMBRAMIENTO N
    ON DATOS.COMPANIA    = N.COMPANIA
   AND DATOS.NUMERO_DCTO = N.DP_NUMEDOCU
   AND DATOS.SUCURSAL    = N.SUCURSAL
  LEFT JOIN ESTADO_CIVIL EC
    ON DATOS.COMPANIA     = EC.COMPANIA
   AND DATOS.ESTADO_CIVIL = EC.ESTADO_CIVIL   
WHERE DATOS.COMPANIA = s$compania$s
  AND DATOS.NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s 
  AND N.ULT_CARGO NOT IN(0)
