SELECT NAT_RELACION_PRUEBAS.COMPANIA,
  NRO_CONVOCATORIA,
  CONSECUTIVO,
  NAT_CLASES_DE_PRUEBA.DESCRIPCION PRUEBA,
   CASE WHEN CARACTER IN ('E') THEN 'ELIMINATORIA' ELSE 'CLASIFICATORIA' END CARACTER,
  VALOR_CONCURSO,
  CALIFICACION,
  EDUCACION,
  EXPERIENCIA
FROM NAT_RELACION_PRUEBAS
INNER JOIN NAT_CLASES_DE_PRUEBA
ON NAT_RELACION_PRUEBAS.COMPANIA =  NAT_CLASES_DE_PRUEBA.COMPANIA
AND NAT_RELACION_PRUEBAS.PRUEBA = NAT_CLASES_DE_PRUEBA.PRUEBA
WHERE NAT_RELACION_PRUEBAS.COMPANIA  = '$P!{PR_COMPANIA}' 
AND NRO_CONVOCATORIA = $P!{PR_NRO_CONVOCATORIA}
