SELECT 
  D.NUMEROCARPETA
 ,D.CODIGO
 ,D.NUMERO_DCTO
 ,(D.APELLIDO1 ||' '|| D.APELLIDO2 ||' '|| D.NOMBRES) NOMBREPERSONA
 ,N.NO_TIPODOCUACTO
 ,N.NO_NUME
 ,N.NO_FECHRESODECR
 ,N.NO_TIPO
 ,N.NO_FECHAEFECTIVIDAD
 ,C.NOMBRE
 ,N.NO_CARGOA
 ,N.NO_CAUSA
FROM NAT_DATOS_PERSONALES D
  LEFT JOIN NAT_NOMBRAMIENTO N
    ON D.COMPANIA    = N.COMPANIA
   AND D.NUMERO_DCTO = N.DP_NUMEDOCU
   AND D.SUCURSAL    = N.SUCURSAL
  LEFT JOIN CAUSARETIRO C
    ON N.COMPANIA = C.COMPANIA
   AND N.NO_TIPO  = C.CODIGO
WHERE D.COMPANIA = s$compania$s
  AND D.NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s  
  AND D.FECHARETIRO IS NOT NULL
