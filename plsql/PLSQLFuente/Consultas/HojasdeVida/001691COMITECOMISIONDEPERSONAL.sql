SELECT
  	PERSONAL.NOMBRECOMPLETO NOMBRESCOMPLETO,
  	   PERSONAL.NUMERO_DCTO,
  	   NAT_REPRESENTANTE.R_CODIGO_PERSONA,
  	   NAT_REPRESENTANTE.FUNCIONES,
	   NAT_REPRESENTANTE.FECHA_VINC_COM,
	   NAT_REPRESENTANTE.FECHA_DESVINC_COM,
	   NAT_CARGO_COMITE.NOMBRE CARGOCOMITE,
	   NAT_CLASE_INTEGRANTE.NOMBRE CLASEINTEGRANTE
FROM NAT_REPRESENTANTE
	INNER JOIN PERSONAL
	   ON NAT_REPRESENTANTE.COMPANIA = PERSONAL.COMPANIA
     AND NAT_REPRESENTANTE.NUMERO_DCTO = PERSONAL.NUMERO_DCTO
     AND NAT_REPRESENTANTE.SUCURSAL = PERSONAL.SUCURSAL
	INNER JOIN NAT_CARGO_COMITE
	   ON NAT_REPRESENTANTE.COMPANIA = NAT_CARGO_COMITE.COMPANIA
	  AND NAT_REPRESENTANTE.CARGOCOMITE = NAT_CARGO_COMITE.CODIGO
	  AND NAT_REPRESENTANTE.TIPOCOMITE = NAT_CARGO_COMITE.CODIGO_COMITE
	INNER JOIN NAT_CLASE_INTEGRANTE
	   ON NAT_REPRESENTANTE.COMPANIA = NAT_CLASE_INTEGRANTE.COMPANIA
	  AND NAT_REPRESENTANTE.CLASEINTEGRANTE = NAT_CLASE_INTEGRANTE.CODIGO
	  AND NAT_REPRESENTANTE.TIPOCOMITE = NAT_CLASE_INTEGRANTE.CODIGO_COMITE
WHERE NAT_REPRESENTANTE.COMPANIA        = s$compania$s
	  AND NAT_REPRESENTANTE.CARGOCOMITE  = s$cargocomite$s
	  AND NAT_REPRESENTANTE.FECHA_DESVINC_COM  IS NOT NULL
ORDER BY PERSONAL.NUMERO_DCTO
