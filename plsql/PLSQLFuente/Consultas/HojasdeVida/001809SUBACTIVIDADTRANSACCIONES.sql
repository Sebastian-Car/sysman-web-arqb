SELECT DISTINCT
   SST_TRANSACCION_ACTIVIDAD.ACTIVIDAD,
  SST_TIPO_ACTIVIDAD.NOMBRE NOMBREACTIVIDAD,
  SST_TRANSACCION_ACTIVIDAD.OBSERVACION
FROM (s$consultaBase$s)TRANSACCION
INNER JOIN SST_TRANSACCION_ACTIVIDAD
    ON TRANSACCION.COMPANIA                   = SST_TRANSACCION_ACTIVIDAD.COMPANIA
    AND TRANSACCION.CODIGO          = SST_TRANSACCION_ACTIVIDAD.TIPO_TRANSACCION
    AND TRANSACCION.CONSECUTIVO               = SST_TRANSACCION_ACTIVIDAD.TRANSACCION
  INNER JOIN SST_TIPO_ACTIVIDAD
    ON SST_TRANSACCION_ACTIVIDAD.COMPANIA           = SST_TIPO_ACTIVIDAD.COMPANIA
    AND SST_TRANSACCION_ACTIVIDAD.TIPO_TRANSACCION  = SST_TIPO_ACTIVIDAD.TIPO_TRANSACCION  
    AND SST_TRANSACCION_ACTIVIDAD.ACTIVIDAD         = SST_TIPO_ACTIVIDAD.CODIGO
WHERE TRANSACCION.COMPANIA          = $P!{PR_COMPANIA} 
  AND TRANSACCION.CODIGO            = $P!{PR_TIPO_TRANSACCION} 
  AND TRANSACCION.CONSECUTIVO       = $P!{PR_CONSECUTIVO}

