SELECT 
	NPA.ANO, 
	NAT.NOMBRE TIPO, 
	NPA.ACTIVIDADES, 
	NPA.AREA, 
	NPA.OBJETIVO, 
	NPA.CUBRIMIENTO, 
	NPA.DURACION, 
	NPA.COSTOESTIMADO, 
	NPA.PERFILPARTICIPANTES, 
	NPA.OBSERVACIONES, 
	NPA.NIVELCARGOS
FROM NAT_PLANEACIONACTIVIDADES NPA 
	 INNER JOIN  NAT_TIPOACTIVIDADES NAT
	    ON NPA.COMPANIA = NAT.COMPANIA
	    AND NPA.TIPO = NAT.CODIGO
	LEFT JOIN NAT_TIPOACTIVIDADES
		ON NPA.COMPANIA = NAT_TIPOACTIVIDADES.COMPANIA
		AND NPA.TIPO = NAT_TIPOACTIVIDADES.CODIGO
WHERE NPA.COMPANIA = s$compania$s
	AND NPA.ANO BETWEEN s$anoInicial$s AND s$anoFinal$s
  	AND NAT_TIPOACTIVIDADES.MENU IN ('2104')
	AND NPA.TIPO = CASE WHEN s$tipoPlaneacion$s IN (8) THEN NPA.TIPO ELSE s$tipoPlaneacion$s END
