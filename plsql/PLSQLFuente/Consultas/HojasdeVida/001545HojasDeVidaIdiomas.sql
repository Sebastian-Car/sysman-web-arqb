SELECT
  NOMBRE
 ,CASE WHEN HABLA   IN('R')   THEN 'X' END HABLAR
 ,CASE WHEN HABLA   IN('B')   THEN 'X' END HABLAB
 ,CASE WHEN HABLA   IN('MB')  THEN 'X' END HABLAM
 ,CASE WHEN LEE     IN('R')   THEN 'X' END LEER
 ,CASE WHEN LEE     IN('B')   THEN 'X' END LEEB
 ,CASE WHEN LEE     IN('MB')  THEN 'X' END LEEM
 ,CASE WHEN ESCRIBE IN('R')   THEN 'X' END ESCRIBER
 ,CASE WHEN ESCRIBE IN('B')   THEN 'X' END ESCRIBEB
 ,CASE WHEN ESCRIBE IN('MB')  THEN 'X' END ESCRIBEM
 ,NOTAS
FROM NAT_IDIOMAS
WHERE COMPANIA    = $P!{PR_COMPANIA} 
  AND NUMERO_DCTO = $P!{PR_NUMERO_DCTO} 
  AND SUCURSAL    = $P!{PR_SUCURSAL} 
