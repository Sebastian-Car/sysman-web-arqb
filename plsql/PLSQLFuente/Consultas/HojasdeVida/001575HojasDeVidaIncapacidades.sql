SELECT
  INCAPACIDADES.AUTO
 ,INCAPACIDADES.FECHA_AUTO
 ,INCAPACIDADES.FECHA_INICIO
 ,INCAPACIDADES.FECHA_FIN
 ,INCAPACIDADES.FECHAEMISION
 ,INCAPACIDADES.ENTIDADQUELAEMITE
 ,INCAPACIDADES.DIAS
 ,TIPO_INCAPACIDAD.DESCRIPCION
 ,INCAPACIDADES.ORIGEN
 ,INCAPACIDADES.NUM_CERTIFICADO
 ,INCAPACIDADES.FECHA_EXPEDICION
 ,INCAPACIDADES.DIAGNOSTICO
 ,INCAPACIDADES.TRANSCRIPCION
 ,INCAPACIDADES.VALOR_LIQUIDADO
FROM INCAPACIDADES 
  LEFT JOIN TIPO_INCAPACIDAD
    ON INCAPACIDADES.COMPANIA    = TIPO_INCAPACIDAD.COMPANIA
   AND INCAPACIDADES.INCAPACIDAD = TIPO_INCAPACIDAD.INCAPACIDAD
WHERE INCAPACIDADES.COMPANIA    = $P!{PR_COMPANIA} 
  AND INCAPACIDADES.NUMERO_DCTO = $P!{PR_NUMERO_DCTO} 
  AND INCAPACIDADES.SUCURSAL    = $P!{PR_SUCURSAL} 
  AND INCAPACIDADES.INCAPACIDAD NOT IN ('04')
