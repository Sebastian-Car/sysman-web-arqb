WITH CONSULTAELEGIBLES AS
  (SELECT NAT_ELEGIBLES.COMPANIA,
    NAT_ELEGIBLES.NUMERO_DCTO,
    NAT_ELEGIBLES.TOTAL,
    NAT_ELEGIBLES.ACEPTO,
    NAT_APERTURA_INSCRITOS.NOMBRE,
    NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA
  FROM NAT_ELEGIBLES
  LEFT JOIN NAT_APERTURA_INSCRITOS
  ON NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA = NAT_ELEGIBLES.NRO_CONVOCATORIA
  AND NAT_APERTURA_INSCRITOS.NUMERO_DCTO     = NAT_ELEGIBLES.NUMERO_DCTO
  WHERE NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA =  s$convocatoria$s
  ORDER BY NAT_ELEGIBLES.TOTAL DESC
  )
SELECT CONSULTAELEGIBLES.NUMERO_DCTO,
  CONSULTAELEGIBLES.TOTAL,
  CONSULTAELEGIBLES.ACEPTO,
  CONSULTAELEGIBLES.NOMBRE,
  CONSULTAELEGIBLES.NRO_CONVOCATORIA,
  NAT_APERTURA.FECHA_CONVOCATORIA,
  CARGOS.NOMBRE_DEL_CARGO AS DENOMINACION,
  NAT_APERTURA.CODIGOCARGO,
  NAT_APERTURA.GRADOCARGO,
  NAT_APERTURA.FECHAINICIOINSCRIPCION,
  NAT_APERTURA.FECHACIERREINSCRIPCION
FROM CONSULTAELEGIBLES
LEFT JOIN NAT_APERTURA
ON CONSULTAELEGIBLES.COMPANIA          = NAT_APERTURA.COMPANIA
AND CONSULTAELEGIBLES.NRO_CONVOCATORIA = NAT_APERTURA.NRO_CONVOCATORIA
LEFT JOIN CARGOS
ON NAT_APERTURA.COMPANIA = CARGOS.COMPANIA
AND NAT_APERTURA.CODIGOCARGO = CARGOS.ID_DE_CARGO
