SELECT 
  NAT_NOMBRAMIENTO.COMPANIA,
	NAT_NOMBRAMIENTO.DP_NUMEDOCU,
	NAT_NOMBRAMIENTO.NO_ACTAPOSE,
	NAT_NOMBRAMIENTO.NO_TIPODOCUACTO,
	NAT_NOMBRAMIENTO.NO_TIPO,
	NAT_NOMBRAMIENTO.NO_SALARION,
	NAT_NOMBRAMIENTO.NO_SALARIOA,
	NAT_NOMBRAMIENTO.NO_NUME,
	NAT_NOMBRAMIENTO.NO_FECHRESODECR,
	NAT_NOMBRAMIENTO.EN_FECHDESDE,
	NAT_NOMBRAMIENTO.EN_FECHHASTA,
	NAT_NOMBRAMIENTO.NO_CARGOA,
	NAT_NOMBRAMIENTO.NO_CARGON,
	NAT_NOMBRAMIENTO.NO_CAUSA,
	NAT_NOMBRAMIENTO.NO_DEPEANTE,
	NAT_NOMBRAMIENTO.NO_DEPENUEV,
	NAT_NOMBRAMIENTO.NO_FECHACTAPOSE,
	NAT_NOMBRAMIENTO.NO_FECHAEFECTIVIDAD,
	FORMANOMBRAMIENTO.NOMBREFORMA
FROM NAT_NOMBRAMIENTO 
	INNER JOIN FORMANOMBRAMIENTO
		ON  NAT_NOMBRAMIENTO.COMPANIA = FORMANOMBRAMIENTO.COMPANIA
		AND NAT_NOMBRAMIENTO.NO_TIPO = FORMANOMBRAMIENTO.IDFORMA
WHERE NAT_NOMBRAMIENTO.COMPANIA = $P{PR_COMPANIA}
AND NAT_NOMBRAMIENTO.DP_NUMEDOCU = $P{PR_DP_NUMEDOCU}
ORDER BY NAT_NOMBRAMIENTO.DP_NUMEDOCU DESC
