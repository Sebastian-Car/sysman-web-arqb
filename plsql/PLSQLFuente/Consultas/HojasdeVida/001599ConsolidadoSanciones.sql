SELECT 
  D.NUMEROCARPETA
 ,D.CODIGO
 ,D.NUMERO_DCTO
 ,(D.APELLIDO1 ||' '|| D.APELLIDO2 ||' '|| D.NOMBRES) NOMBREPERSONA
 ,L.FECHANOVEDAD
 ,L.FECHA_INICIO
 ,L.FECHA_FINAL
 ,L.ANO
 ,L.MES
 ,L.DIAS
 ,L.VALOR
 ,TL.DESCRIPCION
 ,L.TIPOACTO
 ,L.NUMERO_ACTO
 ,L.FECHA_ACTO
 ,L.OBSERVACION_L                                     OBSERVACIONES
FROM NAT_DATOS_PERSONALES D  
 INNER JOIN LICENCIAS L
    ON D.COMPANIA = L.COMPANIA
   AND D.CODIGO   = L.ID_DE_EMPLEADO
 INNER JOIN TIPOS_LICENCIA TL
    ON L.COMPANIA = TL.COMPANIA
   AND L.LICENCIA = TL.LICENCIA
WHERE D.COMPANIA = s$compania$s
  AND D.NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s
