WITH PAISDEPTOCIUDAD AS(
SELECT 
  P.PAIS
 ,D.CODIGO DEPARTAMENTO
 ,C.CODIGO CIUDAD
 ,P.NOMBRE NOMPAIS
 ,D.NOMBRE NOMDEPTO
 ,C.NOMBRE NOMCIUDAD
FROM PAISES P
  INNER JOIN DEPARTAMENTO D
    ON P.PAIS = D.PAIS
  INNER JOIN CIUDAD C
    ON D.CODIGO = C.DEPARTAMENTO
   AND D.PAIS   = C.PAIS
)
SELECT 
  EXPERIENCIA.NUMERO_DCTO
 ,EXPERIENCIA.NOMBRE
 ,PAISDEPTOCIUDAD.NOMDEPTO                                DEPTO
 ,PAISDEPTOCIUDAD.NOMCIUDAD                               NOMBRECIUDAD
 ,EXPERIENCIA.FECHAINGRESO
 ,EXPERIENCIA.FECHARETIRO
 ,DECODE(EXPERIENCIA.DEDICACION, 'TC', 'TIEMPO COMPLETO'
 	                           , 'MT', 'MEDIO TIEMPO'
 	                           , 'TP', 'TIEMPO PARCIAL'
 	                                 , 'OTRA DEDICACIÓN') DEDICACION
 ,EXPERIENCIA.CARGO
FROM NAT_EXPERIENCIA_LABORAL EXPERIENCIA
  LEFT JOIN PAISDEPTOCIUDAD
    ON EXPERIENCIA.PAIS      = PAISDEPTOCIUDAD.PAIS 
   AND EXPERIENCIA.DEPTO     = PAISDEPTOCIUDAD.DEPARTAMENTO 
   AND EXPERIENCIA.MUNICIPIO = PAISDEPTOCIUDAD.CIUDAD 
WHERE EXPERIENCIA.COMPANIA    = $P!{PR_COMPANIA} 
  AND EXPERIENCIA.NUMERO_DCTO = $P!{PR_NUMERO_DCTO} 
  AND EXPERIENCIA.SUCURSAL    = $P!{PR_SUCURSAL}       
ORDER BY EXPERIENCIA.FECHAINGRESO DESC
