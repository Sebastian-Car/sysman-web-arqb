SELECT  NAT_DATOS_PERSONALES.COMPANIA,
        NAT_DATOS_PERSONALES.NUMERO_DCTO,
        NAT_DATOS_PERSONALES.SUCURSAL,
        NAT_DATOS_PERSONALES.NUMEROCARPETA,
        NAT_DATOS_PERSONALES.CODIGO
FROM NAT_DATOS_PERSONALES
WHERE NAT_DATOS_PERSONALES.COMPANIA = s$compania$s
  AND NAT_DATOS_PERSONALES.NUMERO_DCTO BETWEEN 's$empleadoInicial$s' AND 's$empleadoFinal$s'
  AND NAT_DATOS_PERSONALES.NUMEROCARPETA IS NOT NULL
ORDER BY NAT_DATOS_PERSONALES.NUMEROCARPETA
