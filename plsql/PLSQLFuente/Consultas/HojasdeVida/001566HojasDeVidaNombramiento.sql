WITH QRY_DEPENDENCIAANTERIOR AS (
SELECT NAT_NOMBRAMIENTO.DP_NUMEDOCU,
		NAT_NOMBRAMIENTO.NB_CODIGOPERSONA,
		DEPENDENCIA.NOMBRE, 
		NAT_NOMBRAMIENTO.NO_DEPEANTE
FROM NAT_NOMBRAMIENTO 
	LEFT JOIN DEPENDENCIA 
		 ON  NAT_NOMBRAMIENTO.COMPANIA = DEPENDENCIA.COMPANIA
		AND NAT_NOMBRAMIENTO.NO_DEPEANTE = DEPENDENCIA.CODIGO)
SELECT 
                     NAT_NOMBRAMIENTO.COMPANIA,
	NAT_NOMBRAMIENTO.PRIMATECNICANFACTOR,
	NAT_NOMBRAMIENTO.PRIMATECNICAFACTOR,
	NAT_NOMBRAMIENTO.PRIMAALTAGESTION,
	NAT_NOMBRAMIENTO.NO_TIPODOCUACTO,
	NAT_NOMBRAMIENTO.DP_NUMEDOCU,
	NAT_NOMBRAMIENTO.EN_FECHDESDE,
	NAT_NOMBRAMIENTO.EN_FECHHASTA,
	NAT_NOMBRAMIENTO.EVALUADOPRUEBA,
	NAT_NOMBRAMIENTO.GASTOSREPRESENTACION,
	NAT_NOMBRAMIENTO.NB_CODIGOPERSONA,
	NVL(NAT_NOMBRAMIENTO.NO_ACTAPOSE, '')NO_ACTAPOSE,
	NAT_NOMBRAMIENTO.NO_CARGOA,
	NAT_NOMBRAMIENTO.NO_CARGON,
	NAT_NOMBRAMIENTO.NO_CAUSA,
	NAT_NOMBRAMIENTO.NO_CODIGOCARGO,
	NAT_NOMBRAMIENTO.NO_DEPEANTE,
	NAT_NOMBRAMIENTO.NO_DEPENUEV,
	NAT_NOMBRAMIENTO.NO_FECHACARGO, 
	NVL(TO_CHAR (NAT_NOMBRAMIENTO.NO_FECHACTAPOSE,'DD/MM/YYYY'),' ')NO_FECHACTAPOSE,
	NAT_NOMBRAMIENTO.NO_FECHAEFECTIVIDAD,
	NAT_NOMBRAMIENTO.NO_FECHRESODECR,
	NAT_NOMBRAMIENTO.NO_GRADOCARGO,
	NAT_NOMBRAMIENTO.NO_NUME,
	NAT_NOMBRAMIENTO.NO_SALARIOA,
	NAT_NOMBRAMIENTO.NO_SALARION,
	NAT_NOMBRAMIENTO.NO_TIPO,
	NAT_NOMBRAMIENTO.NO_AUXT,
	NAT_NOMBRAMIENTO.ESTADO_VINCULACION,
	NAT_NOMBRAMIENTO.NO_AUXA,
	FORMANOMBRAMIENTO.NOMBREFORMA TN_NOMBRE,
	FORMANOMBRAMIENTO.IDFORMA,
	DEPENDENCIA.NOMBRE,
	DEPENDENCIA.CODIGO DEPENDENCIA,
	CASE 
		WHEN NAT_NOMBRAMIENTO.ESTADO_VINCULACION IN (-1) 
		THEN 'NUEVO'
		ELSE 'ANTIGUO'
	END ESTADO_VINCULACION,
	NAT_NOMBRAMIENTO.NO_AUXT, NAT_NOMBRAMIENTO.NO_AUXA, 
	QRY_DEPENDENCIAANTERIOR.NOMBRE AS DEPENDENCIAANTERIOR, 
	QRY_DEPENDENCIAANTERIOR.NO_DEPEANTE AS CODDEPANTERIOR, 
	NAT_NOMBRAMIENTO.NB_CODIGOPERSONA
FROM NAT_NOMBRAMIENTO 
	LEFT JOIN FORMANOMBRAMIENTO 
		ON  NAT_NOMBRAMIENTO.COMPANIA = FORMANOMBRAMIENTO.COMPANIA
		AND NAT_NOMBRAMIENTO.NO_TIPO = FORMANOMBRAMIENTO.IDFORMA
	LEFT JOIN DEPENDENCIA
		ON  NAT_NOMBRAMIENTO.COMPANIA = DEPENDENCIA.COMPANIA
		AND NAT_NOMBRAMIENTO.NO_DEPENUEV = DEPENDENCIA.CODIGO
	LEFT JOIN QRY_DEPENDENCIAANTERIOR 
		ON  NAT_NOMBRAMIENTO.NB_CODIGOPERSONA = QRY_DEPENDENCIAANTERIOR.NB_CODIGOPERSONA 
		AND NAT_NOMBRAMIENTO.NO_DEPEANTE = QRY_DEPENDENCIAANTERIOR.NO_DEPEANTE
WHERE NAT_NOMBRAMIENTO.COMPANIA = $P{PR_COMPANIA}
AND NAT_NOMBRAMIENTO.RETIRO_O_NOMBRAMIENTO = 'B'
AND NAT_NOMBRAMIENTO.DP_NUMEDOCU = $P{PR_DP_NUMEDOCU}
ORDER BY NAT_NOMBRAMIENTO.NO_FECHACTAPOSE DESC
