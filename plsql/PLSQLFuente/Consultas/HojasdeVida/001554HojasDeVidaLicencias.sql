MERGE INTO CONSULTAS FIN USING (SELECT '001554HojasDeVidaLicencias' INFORME ,TO_CLOB(q'[SELECT   
  LICENCIAS.NUMERO_ACTO,
  NAT_TIPO_ACTO_ADTIVO.TIPOACTOADTIVO
 ,NVL(TO_CHAR(LICENCIAS.FECHA_ACTO,'DD/MM/YYYY'),' ') FECHA_ACTO
 ,LICENCIAS.FECHA_INICIO
 ,LICENCIAS.FECHA_FINAL
 ,LICENCIAS.VALOR
 ,TIPO.DESCRIPCION
 ,LICENCIAS.ANO
 ,LICENCIAS.MES
 ,LICENCIAS.DIAS
 ,LICENCIAS.FECHANOVEDAD
 ,LICENCIAS.TIPOACTO
 ,LICENCIAS.OBSERVACION_L OBSERVACIONES
FROM LICENCIAS 
  LEFT JOIN TIPOS_LICENCIA TIPO
    ON LICENCIAS.COMPANIA = TIPO.COMPANIA
   AND LICENCIAS.LICENCIA = TIPO.LICENCIA
INNER JOIN NAT_TIPO_ACTO_ADTIVO
ON LICENCIAS.TIPOACTO  = NAT_TIPO_ACTO_ADTIVO.CODIGO 
WHERE LICENCIAS.COMPANIA    = $P!{PR_COMPANIA}
AND LICENCIAS.NUMERO_DCTO = '$P!{PR_NUMERO_DCTO}
'
AND LICENCIAS.SUCURSAL    = $P!{PR_SUCURSAL}
ORDER BY LICENCIAS.FECHA_ACTO DESC]') CONSULTA, 21 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);

MERGE INTO CONSULTAS_SUB FIN USING (SELECT '001551HojasDeVidaSalarios' INFORME_PADRE, '001554HojasDeVidaLicencias' INFORME_HIJO, 'PR_STRSQL_HOJASDEVIDA_LICENCIAS' PARAMETRO ,NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (FIN.INFORME_PADRE = INI.INFORME_PADRE AND INI.INFORME_HIJO = FIN.INFORME_HIJO)  WHEN MATCHED THEN  UPDATE SET FIN.PARAMETRO = INI.PARAMETRO , FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME_PADRE,INFORME_HIJO, PARAMETRO,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME_PADRE,INI.INFORME_HIJO, INI.PARAMETRO,INI.CREATED_BY,SYSDATE);


