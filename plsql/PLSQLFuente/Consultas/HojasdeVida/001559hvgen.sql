SELECT 
	NAT_NOMBRAMIENTO.DP_NUMEDOCU,
	NAT_NOMBRAMIENTO.NO_SALARIOA,
                     NAT_NOMBRAMIENTO.NO_SALARION,
	NAT_NOMBRAMIENTO.NO_GRADOCARGO,
	NAT_NOMBRAMIENTO.RETIRO_O_NOMBRAMIENTO,
	NAT_NOMBRAMIENTO.EN_FECHHASTA,
	NAT_NOMBRAMIENTO.NO_FECHACTAPOSE,
	NAT_NOMBRAMIENTO.NO_ACTAPOSE,
	NAT_DATOS_PERSONALES.NOMBRES,
	NAT_DATOS_PERSONALES.NUMERO_DCTO,
	NAT_DATOS_PERSONALES.NUMEROCARPETA,
	NAT_DATOS_PERSONALES.APELLIDO1,
	NAT_DATOS_PERSONALES.APELLIDO2,
	FORMANOMBRAMIENTO.NOMBREFORMA TN_NOMBRE 
FROM NAT_DATOS_PERSONALES 
	INNER JOIN NAT_NOMBRAMIENTO 
		ON  NAT_DATOS_PERSONALES.COMPANIA = NAT_NOMBRAMIENTO.COMPANIA
		AND NAT_DATOS_PERSONALES.NUMERO_DCTO = NAT_NOMBRAMIENTO.DP_NUMEDOCU
		AND NAT_DATOS_PERSONALES.SUCURSAL = NAT_NOMBRAMIENTO.SUCURSAL
	LEFT JOIN FORMANOMBRAMIENTO 
		ON  NAT_NOMBRAMIENTO.COMPANIA = FORMANOMBRAMIENTO.COMPANIA
		AND NAT_NOMBRAMIENTO.NO_TIPO = FORMANOMBRAMIENTO.IDFORMA
WHERE NAT_DATOS_PERSONALES.COMPANIA = s$compania$s
  AND NAT_DATOS_PERSONALES.NUMERO_DCTO IS NOT NULL 
  AND NAT_DATOS_PERSONALES.NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s
ORDER BY NAT_DATOS_PERSONALES.NUMERO_DCTO

