SELECT
                     NAT_DATOS_PERSONALES.COMPANIA,
	NAT_DATOS_PERSONALES.NUMERO_DCTO,
	TIPOS_LICENCIA.LICENCIA,
	TIPOS_LICENCIA.REMUNERACION,
	TIPOS_LICENCIA.DESCRIPCION,
	TIPOS_LICENCIA.ID_DE_CONCEPTO,
	LICENCIAS.FECHA_ACTO,
	LICENCIAS.FECHA_INICIO,
	LICENCIAS.VALOR,
	LICENCIAS.TIPOACTO,
	LICENCIAS.ANO,
	LICENCIAS.FECHANOVEDAD,
	LICENCIAS.DIAS,
	LICENCIAS.FECHA_FINAL,
	LICENCIAS.ID_DE_PROCESO,
	LICENCIAS.MES,
	LICENCIAS.NUMERO_ACTO,
	LICENCIAS.NUMEROPERIODO,
	LICENCIAS.OBSERVACION_L,
	LICENCIAS.PERIODO,
	PERSONAL.ID_DE_EMPLEADO,
	NAT_DATOS_PERSONALES.OBSERVACIONES
FROM TIPOS_LICENCIA
	INNER JOIN LICENCIAS
		ON TIPOS_LICENCIA.COMPANIA = LICENCIAS.COMPANIA
		AND TIPOS_LICENCIA.LICENCIA = LICENCIAS.LICENCIA
	INNER JOIN PERSONAL
		ON PERSONAL.COMPANIA = LICENCIAS.COMPANIA
		AND PERSONAL.ID_DE_EMPLEADO = LICENCIAS.ID_DE_EMPLEADO
	INNER JOIN NAT_DATOS_PERSONALES
		ON NAT_DATOS_PERSONALES.COMPANIA = PERSONAL.COMPANIA
		AND NAT_DATOS_PERSONALES.NUMERO_DCTO = PERSONAL.NUMERO_DCTO
WHERE NAT_DATOS_PERSONALES.COMPANIA = $P{PR_COMPANIA}
		AND	TIPOS_LICENCIA.REMUNERACION IN (0)
		AND NAT_DATOS_PERSONALES.NUMERO_DCTO = $P{PR_NUMERO_DCTO}
ORDER BY NAT_DATOS_PERSONALES.NUMERO_DCTO DESC
