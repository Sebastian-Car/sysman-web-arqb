   SELECT NAT_NOMBRAMIENTO.DP_NUMEDOCU,
  NAT_DATOS_PERSONALES.APELLIDO1 || ' ' || NAT_DATOS_PERSONALES.APELLIDO2 || ' '|| NAT_DATOS_PERSONALES.NOMBRES NOMBRE,
  CASE WHEN NAT_NOMBRAMIENTO.EVALUADOPRUEBA NOT IN (0)
       THEN 'X'
       ELSE ''
  END EVALUADOPRUEBA,
  NAT_NOMBRAMIENTO.NO_TIPODOCUACTO,  
  NAT_TIPO_ACTO_ADTIVO.TIPOACTOADTIVO, 
  NAT_NOMBRAMIENTO.NO_NUME,
  NAT_NOMBRAMIENTO.NO_FECHRESODECR,
  NAT_NOMBRAMIENTO.NO_CARGOA,
  NAT_NOMBRAMIENTO.NO_SALARIOA,
  NAT_NOMBRAMIENTO.NO_CODIGOCARGO,
  NAT_NOMBRAMIENTO.NO_GRADOCARGO,
  NAT_NOMBRAMIENTO.NO_CARGON,
  NAT_NOMBRAMIENTO.NO_SALARION,
  NAT_NOMBRAMIENTO.NO_ACTAPOSE,
  FORMANOMBRAMIENTO.NOMBREFORMA TN_NOMBRE,
  NAT_NOMBRAMIENTO.NO_FECHACTAPOSE,
  NAT_NOMBRAMIENTO.NO_CAUSA,
  NAT_NOMBRAMIENTO.NO_DEPEANTE,
  NAT_NOMBRAMIENTO.NO_DEPENUEV,
  NAT_NOMBRAMIENTO.EN_FECHDESDE,
  NAT_NOMBRAMIENTO.EN_FECHHASTA,
  NAT_NOMBRAMIENTO.NO_FECHAEFECTIVIDAD
FROM NAT_NOMBRAMIENTO
  LEFT JOIN NAT_DATOS_PERSONALES
    ON NAT_NOMBRAMIENTO.COMPANIA    = NAT_DATOS_PERSONALES.COMPANIA
   AND NAT_NOMBRAMIENTO.DP_NUMEDOCU = NAT_DATOS_PERSONALES.NUMERO_DCTO
   AND NAT_NOMBRAMIENTO.SUCURSAL    = NAT_DATOS_PERSONALES.SUCURSAL
  LEFT JOIN NAT_TIPO_ACTO_ADTIVO
    ON NAT_NOMBRAMIENTO.NO_TIPODOCUACTO = NAT_TIPO_ACTO_ADTIVO.CODIGO
  LEFT JOIN FORMANOMBRAMIENTO
    ON NAT_NOMBRAMIENTO.COMPANIA = FORMANOMBRAMIENTO.COMPANIA
   AND NAT_NOMBRAMIENTO.NO_TIPO  = FORMANOMBRAMIENTO.IDFORMA  
WHERE NAT_NOMBRAMIENTO.COMPANIA  = s$compania$s        
  AND NAT_DATOS_PERSONALES.NUMERO_DCTO BETWEEN 's$empleadoInicial$s' AND 's$empleadoFinal$s'
