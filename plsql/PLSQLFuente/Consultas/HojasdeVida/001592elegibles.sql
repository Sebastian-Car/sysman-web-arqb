SELECT 
    NAT_ELEGIBLES.NRO_CONVOCATORIA, 
    NAT_APERTURA_INSCRITOS.NOMBRE, 
    NAT_APERTURA.FECHA_CONVOCATORIA, 
    NAT_APERTURA.CLASE, 
    NAT_APERTURA.PLAZAS, 
    NAT_APERTURA.LUGAR_INSCRIPCION, 
    NAT_APERTURA.HORA_ATENCION, 
    NAT_APERTURA.FECHA_CONCURSO, 
    NAT_APERTURA.LUGAR_CONCURSO, 
    NAT_APERTURA.HORA_CONCURSO, 
    NAT_APERTURA.PUBLICACION, 
    NAT_APERTURA.SUELDO, 
    NAT_APERTURA.REQUISITOEDUCACION, 
    NAT_APERTURA.REQUISITOEXPERIENCIA, 
    NAT_APERTURA.OBSERVACIONESREQUISITOS, 
    NAT_APERTURA.REQUISITOEQUIVALENCIAS, 
    NAT_APERTURA.FECHAINICIOINSCRIPCION, 
    NAT_APERTURA.FECHACIERREINSCRIPCION, 
    NAT_APERTURA.UBICACIONORGANICA, 
    NAT_APERTURA.LUGARTRABAJO, 
    NAT_APERTURA.NOTAS, 
    NAT_APERTURA.CODIGOCARGO, 
    NAT_APERTURA.GRADOCARGO, 
    NAT_APERTURA.FECHACARGO, 
    NAT_APERTURA.CREATED_BY, 
    NAT_APERTURA.DATE_CREATED, 
    NAT_APERTURA.MODIFIED_BY, 
    NAT_APERTURA.DATE_MODIFIED,     
    NAT_APERTURA_INSCRITOS.NUMERO_DCTO,
    CARGOS.NOMBRE_DEL_CARGO DENOMINACION
FROM NAT_APERTURA 
INNER JOIN CARGOS
      ON   NAT_APERTURA.COMPANIA = CARGOS.COMPANIA 
      AND  NAT_APERTURA.CODIGOCARGO = CARGOS.ID_DE_CARGO
INNER JOIN NAT_ELEGIBLES 
        ON NAT_APERTURA.COMPANIA         = NAT_ELEGIBLES.COMPANIA
       AND NAT_APERTURA.NRO_CONVOCATORIA = NAT_ELEGIBLES.NRO_CONVOCATORIA
INNER JOIN NAT_APERTURA_INSCRITOS 
        ON NAT_ELEGIBLES.COMPANIA 			= NAT_APERTURA_INSCRITOS.COMPANIA
       AND NAT_ELEGIBLES.NRO_CONVOCATORIA 	= NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA
       AND NAT_ELEGIBLES.NUMERO_DCTO 		= NAT_APERTURA_INSCRITOS.NUMERO_DCTO
       AND NAT_ELEGIBLES.SUCURSAL 			= NAT_APERTURA_INSCRITOS.SUCURSAL
WHERE NAT_APERTURA.COMPANIA = s$compania$s
  AND NAT_APERTURA.NRO_CONVOCATORIA = s$nroConvocatoria$s
