SELECT * FROM (
SELECT 
  DEPENDENCIA.NOMBRE,
  SST_PROCESOS.PROCESO || '-' || SST_PROCESOS.CODIGO PROCESO,
  SST_PROCESOS.ZONA_LUGAR,
  SST_PROCESOS.ACTIVIDADES,
  SST_PROCESOS.TAREAS,
  CASE SST_PROCESOS.RUTINARIAS WHEN -1 THEN 'SI' ELSE 'NO' END RUTINARIAS,
  SST_DETALLE_MATRIZ_RIESGOS.FACTOR_RIESGO,
  SST_DETALLE_MATRIZ_RIESGOS.VALOR_TEXTO
FROM SST_DETALLE_MATRIZ_RIESGOS
     INNER JOIN SST_PROCESOS
      ON SST_DETALLE_MATRIZ_RIESGOS.COMPANIA = SST_PROCESOS.COMPANIA
      AND SST_DETALLE_MATRIZ_RIESGOS.PROCESO = SST_PROCESOS.CODIGO
     INNER JOIN DEPENDENCIA 
      ON SST_PROCESOS.COMPANIA = DEPENDENCIA.COMPANIA 
      AND SST_PROCESOS.DEPENDENCIA = DEPENDENCIA.CODIGO
WHERE SST_DETALLE_MATRIZ_RIESGOS.COMPANIA =  s$compania$s
AND SST_DETALLE_MATRIZ_RIESGOS.VERSION =  's$version$s'
ORDER BY SST_DETALLE_MATRIZ_RIESGOS.GRUPO, SST_DETALLE_MATRIZ_RIESGOS.FACTOR_RIESGO)
PIVOT (MAX(VALOR_TEXTO) FOR FACTOR_RIESGO IN (s$factores$s))
