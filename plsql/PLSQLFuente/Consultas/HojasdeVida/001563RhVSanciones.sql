SELECT LICENCIAS.ID_DE_EMPLEADO,
  PERSONAL.NUMERO_DCTO ,
  PERSONAL.NOMBRECOMPLETO,
  LICENCIAS.LICENCIA,
  LICENCIAS.FECHA_INICIO,
  LICENCIAS.FECHA_FINAL,
  LICENCIAS.NUMERO_ACTO,
 
  LICENCIAS.FECHA_ACTO,
  LICENCIAS.DIAS,
  LICENCIAS.TIPOACTO,
  LICENCIAS.VALOR,
  LICENCIAS.FECHANOVEDAD,
  LICENCIAS.OBSERVACION_L OBSERVACIONES,
  TIPOS_LICENCIA.DESCRIPCION
FROM NAT_DATOS_PERSONALES
INNER JOIN PERSONAL
     ON NAT_DATOS_PERSONALES.COMPANIA     = PERSONAL.COMPANIA
    AND NAT_DATOS_PERSONALES.NUMERO_DCTO = PERSONAL.NUMERO_DCTO
    AND NAT_DATOS_PERSONALES.SUCURSAL = PERSONAL.SUCURSAL
  INNER JOIN LICENCIAS
     ON LICENCIAS.COMPANIA        = NAT_DATOS_PERSONALES.COMPANIA
    AND LICENCIAS.ID_DE_EMPLEADO = NAT_DATOS_PERSONALES.CODIGO
    INNER JOIN TIPOS_LICENCIA
     ON TIPOS_LICENCIA.COMPANIA       = LICENCIAS.COMPANIA
    AND LICENCIAS.LICENCIA           = TIPOS_LICENCIA.LICENCIA
WHERE TIPOS_LICENCIA.COMPANIA    ='s$compania$s'
  AND TIPOS_LICENCIA.REMUNERACION IN (0)
  AND NAT_DATOS_PERSONALES.NUMERO_DCTO BETWEEN 's$empleadoInicial$s' AND 's$empleadoFinal$s'
