SELECT 
 NVL(P01,0) P01,
 NVL(P02,0) P02,
 NVL(P03,0) P03,
 NVL(P04,0) P04,
 NVL(P05,0) P05,
 FECHA_CONVOCATORIA,
 FECHAINICIOINSCRIPCION,
 FECHACIERREINSCRIPCION,
 NRO_CONVOCATORIA,
 NUMERO_DCTO,
 FECHA_RECIBO,
 FOLIOS,
 NRO_FORMULARIO,
 DENOMINACION,
 CODIGOCARGO, 
 CASE WHEN  RECHAZOEXPERIENCIA NOT IN (0) THEN 'X' ELSE ' ' END RECHAZOEXPERIENCIA,
 CASE WHEN  RECHAZOESTUDIOS NOT IN (0) THEN 'X' ELSE ' ' END RECHAZOESTUDIOS,
 CASE WHEN  RECHAZOOTROS NOT IN (0) THEN 'X' ELSE ' ' END RECHAZOOTROS, 
 NOMBRECOMPLETO,
 GRADOCARGO
 FROM (

 SELECT 
 NAT_APERTURA_INSCRITOS.NOMBRE NOMBRECOMPLETO,
 NVL(NAT_CALIFICACION_PRUEBAS.PRUEBA,0) PRUEBA,
 NVL(NAT_CALIFICACION_PRUEBAS.CALIFICACION,0) CALIFICACION,
 NAT_APERTURA.FECHA_CONVOCATORIA,
    NAT_APERTURA.FECHAINICIOINSCRIPCION,
    NAT_APERTURA.FECHACIERREINSCRIPCION,
    NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA,
    NAT_APERTURA_INSCRITOS.NUMERO_DCTO,
    NAT_APERTURA_INSCRITOS.FECHA_RECIBO,
    NAT_APERTURA_INSCRITOS.FOLIOS,
    NAT_APERTURA_INSCRITOS.NRO_FORMULARIO,

    CARGOS.NOMBRE_DEL_CARGO AS DENOMINACION,
    CARGOS.ID_DE_CARGO CODIGOCARGO,
    CARGOS.GRADO GRADOCARGO,
    NAT_APERTURA_INSCRITOS.RECHAZOEXPERIENCIA,
    NAT_APERTURA_INSCRITOS.RECHAZOESTUDIOS,
    NAT_APERTURA_INSCRITOS.RECHAZOOTROS
  FROM NAT_APERTURA
  INNER JOIN CARGOS
  ON NAT_APERTURA.COMPANIA = CARGOS.COMPANIA
  AND NAT_APERTURA.CODIGOCARGO = CARGOS.ID_DE_CARGO  
  INNER JOIN NAT_APERTURA_INSCRITOS
  ON NAT_APERTURA.COMPANIA = NAT_APERTURA_INSCRITOS.COMPANIA
  AND NAT_APERTURA.NRO_CONVOCATORIA = NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA
  LEFT JOIN NAT_CALIFICACION_PRUEBAS
  ON NAT_APERTURA_INSCRITOS.COMPANIA      = NAT_CALIFICACION_PRUEBAS.COMPANIA
  AND NAT_APERTURA_INSCRITOS.NUMERO_DCTO          = NAT_CALIFICACION_PRUEBAS.NUMERO_DCTO
  AND NAT_APERTURA_INSCRITOS.SUCURSAL          = NAT_CALIFICACION_PRUEBAS.SUCURSAL
  AND NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA      = NAT_CALIFICACION_PRUEBAS.NRO_CONVOCATORIA

 WHERE  NAT_APERTURA_INSCRITOS.COMPANIA =s$compania$s
AND NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA=s$convocatoria$s

  GROUP BY 
 NAT_APERTURA_INSCRITOS.NOMBRE,
  NAT_CALIFICACION_PRUEBAS.PRUEBA,
  NAT_APERTURA.FECHA_CONVOCATORIA,
    NAT_APERTURA.FECHAINICIOINSCRIPCION,
    NAT_APERTURA.FECHACIERREINSCRIPCION,
    NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA,
    NAT_APERTURA_INSCRITOS.NUMERO_DCTO,
    NAT_APERTURA_INSCRITOS.FECHA_RECIBO,
    NAT_APERTURA_INSCRITOS.FOLIOS,
    NAT_APERTURA_INSCRITOS.NRO_FORMULARIO,
    NAT_Calificacion_Pruebas.Calificacion,
    CARGOS.NOMBRE_DEL_CARGO,
    CARGOS.ID_DE_CARGO,
    CARGOS.GRADO,
    NAT_APERTURA_INSCRITOS.RECHAZOEXPERIENCIA,
    NAT_APERTURA_INSCRITOS.RECHAZOESTUDIOS,
    NAT_APERTURA_INSCRITOS.RECHAZOOTROS)
    PIVOT (SUM(NVL(CALIFICACION,0)) FOR PRUEBA IN ('01' P01,'02' P02,'03' P03,'04' P04,'05' P05))
s$ordenamiento$s
