AND NAT_EVALUACION.SUCURSAL    =NAT_DATOS_PERSONALES.SUCURSAL
WHERE NAT_EVALUACION.EV_FORMATO='s$formato$s'
AND NAT_DATOS_PERSONALES.NUMEROCARPETA BETWEEN 's$carpetaInicial$s' AND 's$carpetaFinal$s'
