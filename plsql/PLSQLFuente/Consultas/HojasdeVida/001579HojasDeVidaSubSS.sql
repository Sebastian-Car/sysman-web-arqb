SELECT 
  SS.SS_REGIMEN
 ,FONDO.NOMBRE_FONDO
 ,SS.SS_NRORADICACION
 ,SS.SS_FECHVINC
 ,SS.SS_FECHARADICACION
 ,SS.SS_NOVEDAD
FROM NAT_SEGURIDAD_SOCIAL SS
  LEFT JOIN FONDO
    ON SS.COMPANIA   = FONDO.COMPANIA
   AND SS.SS_ENTIDAD = FONDO.ID_DE_FONDO
WHERE SS.COMPANIA    = $P!{PR_COMPANIA} 
  AND SS.DP_NUMEDOCU = $P!{PR_NUMERO_DCTO} 
  AND SS.SUCURSAL    = $P!{PR_SUCURSAL} 
