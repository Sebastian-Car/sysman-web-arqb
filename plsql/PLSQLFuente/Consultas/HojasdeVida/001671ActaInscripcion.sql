SELECT NAT_APERTURA.COMPANIA, 
       NAT_APERTURA_INSCRITOS.SUCURSAL, 
       NAT_APERTURA.FECHA_CONVOCATORIA, 
       NAT_APERTURA.FECHAINICIOINSCRIPCION, 
       NAT_APERTURA.FECHACIERREINSCRIPCION, 
       NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA, 
       NAT_APERTURA_INSCRITOS.NUMERO_DCTO, 
       NAT_APERTURA_INSCRITOS.FECHA_RECIBO, 
       NAT_APERTURA_INSCRITOS.FOLIOS, 
       NAT_APERTURA_INSCRITOS.NRO_FORMULARIO, 
       NAT_APERTURA_INSCRITOS.NOMBRE, 
       NAT_APERTURA_INSCRITOS.TELEFONO, 
       NAT_APERTURA_INSCRITOS.DIRECCION, 
       CARGOS.NOMBRE_DEL_CARGO AS DENOMINACION, 
       CARGOS.ID_DE_CARGO, 
       CARGOS.GRADO
FROM NAT_APERTURA 
      INNER JOIN CARGOS 
	           ON NAT_APERTURA.COMPANIA = CARGOS.COMPANIA 
			      AND NAT_APERTURA.CODIGOCARGO = CARGOS.ID_DE_CARGO 
			      AND NAT_APERTURA.GRADOCARGO = CARGOS.GRADO 
	         INNER JOIN NAT_APERTURA_INSCRITOS 
			             ON NAT_APERTURA.COMPANIA = NAT_APERTURA_INSCRITOS.COMPANIA 
			           AND NAT_APERTURA.NRO_CONVOCATORIA = NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA
WHERE NAT_APERTURA.COMPANIA=s$compania$s
	    AND NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA=s$convocatoria$s
ORDER BY NAT_APERTURA_INSCRITOS.NRO_FORMULARIO
