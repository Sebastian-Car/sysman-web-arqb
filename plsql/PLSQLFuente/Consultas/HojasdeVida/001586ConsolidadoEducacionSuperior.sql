SELECT 
  DATOS.NUMEROCARPETA
 ,DATOS.CODIGO
 ,DATOS.NUMERO_DCTO
 ,(DATOS.APELLIDO1 ||' '|| DATOS.APELLIDO2 ||' '|| DATOS.NOMBRES) NOMBREPERSONA
 ,CASE WHEN FORMACION.GRADUADO IN(0) THEN 'NO' ELSE 'SI' END GRADUADO
 ,FORMACION.MODALIDAD
 ,FORMACION.ESTABLECIMIENTO
 ,FORMACION.MUNICIPIO
 ,FORMACION.TITULOOBTENIDO
 ,FORMACION.FECHAINICIO
 ,FORMACION.FECHATERMINACION
 ,FORMACION.NIVELAPROBADO
 ,FORMACION.TARJETAPROFESIONAL
 ,FORMACION.HORAS
 ,FORMACION.DIAINICIO
 ,FORMACION.MESINICIO
 ,FORMACION.ANOINICIO
 ,FORMACION.DIATERMINACION
 ,FORMACION.MESTERMINACION
 ,FORMACION.ANOTERMINACION
 ,MODALIDAD.MO_NOMBRE
FROM NAT_DATOS_PERSONALES DATOS
  LEFT JOIN NAT_FORMACION_ACADEMICA FORMACION
    ON DATOS.COMPANIA    = FORMACION.COMPANIA
   AND DATOS.NUMERO_DCTO = FORMACION.NUMERO_DCTO
   AND DATOS.SUCURSAL    = FORMACION.SUCURSAL
  LEFT JOIN NAT_MODALIDAD_ESTUDIOSUPERIOR MODALIDAD
    ON FORMACION.MODALIDAD = MODALIDAD.MO_CODIGO
WHERE DATOS.COMPANIA = s$compania$s
  AND DATOS.NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s
GROUP BY 
  DATOS.NUMEROCARPETA
 ,DATOS.CODIGO
 ,DATOS.NUMERO_DCTO
 ,(DATOS.APELLIDO1 ||' '|| DATOS.APELLIDO2 ||' '|| DATOS.NOMBRES)
 ,CASE WHEN FORMACION.GRADUADO IN(0) THEN 'NO' ELSE 'SI' END   
 ,FORMACION.MODALIDAD
 ,FORMACION.ESTABLECIMIENTO
 ,FORMACION.MUNICIPIO
 ,FORMACION.TITULOOBTENIDO
 ,FORMACION.FECHAINICIO
 ,FORMACION.FECHATERMINACION
 ,FORMACION.NIVELAPROBADO
 ,FORMACION.TARJETAPROFESIONAL
 ,FORMACION.HORAS
 ,FORMACION.DIAINICIO
 ,FORMACION.MESINICIO
 ,FORMACION.ANOINICIO
 ,FORMACION.DIATERMINACION
 ,FORMACION.MESTERMINACION
 ,FORMACION.ANOTERMINACION
 ,MODALIDAD.MO_NOMBRE
