SELECT DISTINCT 
	APERTURA_INSCRITOS.NRO_CONVOCATORIA, 
	APERTURA_INSCRITOS.NUMERO_DCTO, 
	APERTURA_INSCRITOS.FECHA_RECIBO, 
	APERTURA_INSCRITOS.FOLIOS, 
	APERTURA_INSCRITOS.NRO_FORMULARIO,
  APERTURA_INSCRITOS.NOMBRE, 
	APERTURA_INSCRITOS.TELEFONO, 
	APERTURA_INSCRITOS.DIRECCION, 
	APERTURA_INSCRITOS.OBSERVACIONES, 
	APERTURA.FECHA_CONVOCATORIA, 
	APERTURA.CLASE, 
	APERTURA.PLAZAS, 
	APERTURA.DENOMINACION, 
	APERTURA.LUGAR_INSCRIPCION, 
	APERTURA.HORA_ATENCION, 
	APERTURA.FECHA_CONCURSO, 
	APERTURA.LUGAR_CONCURSO, 
	APERTURA.HORA_CONCURSO, 
	APERTURA.PUBLICACION, 
	APERTURA.SUELDO, 
	APERTURA.FECHAINICIOINSCRIPCION, 
	APERTURA.FECHACIERREINSCRIPCION, 
	APERTURA.UBICACIONORGANICA, 
	APERTURA.LUGARTRABAJO, 
	HVCARGOS.NOMBRE_DEL_CARGO  DENOMINACION_CARGO, 
	APERTURA.CODIGOCARGO, 
	APERTURA.GRADOCARGO, 
	RELACION_PRUEBAS.CONSECUTIVO, 
	CALIFICACION_PRUEBAS.CALIFICACION, 
	CALIFICACION_PRUEBAS.PRUEBA, 
	RELACION_PRUEBAS.PUNMAX, 
	CLASES_DE_PRUEBA.DESCRIPCION
FROM CARGOS HVCARGOS 
INNER JOIN NAT_APERTURA APERTURA
   ON HVCARGOS.COMPANIA    = APERTURA.COMPANIA 
  AND HVCARGOS.ID_DE_CARGO = APERTURA.CODIGOCARGO
INNER JOIN NAT_RELACION_PRUEBAS RELACION_PRUEBAS 
   ON APERTURA.COMPANIA 		= RELACION_PRUEBAS.COMPANIA       
  AND APERTURA.NRO_CONVOCATORIA = RELACION_PRUEBAS.NRO_CONVOCATORIA
INNER JOIN NAT_CALIFICACION_PRUEBAS CALIFICACION_PRUEBAS 
  ON RELACION_PRUEBAS.COMPANIA 			= CALIFICACION_PRUEBAS.COMPANIA 
  AND RELACION_PRUEBAS.NRO_CONVOCATORIA = CALIFICACION_PRUEBAS.NRO_CONVOCATORIA 
  AND RELACION_PRUEBAS.PRUEBA 			= CALIFICACION_PRUEBAS.PRUEBA 
INNER JOIN NAT_APERTURA_INSCRITOS APERTURA_INSCRITOS
  ON CALIFICACION_PRUEBAS.COMPANIA 			= APERTURA_INSCRITOS.COMPANIA 
  AND CALIFICACION_PRUEBAS.NRO_CONVOCATORIA = APERTURA_INSCRITOS.NRO_CONVOCATORIA 
  AND CALIFICACION_PRUEBAS.NUMERO_DCTO 		= APERTURA_INSCRITOS.NUMERO_DCTO 
INNER JOIN NAT_CLASES_DE_PRUEBA CLASES_DE_PRUEBA 
  ON CLASES_DE_PRUEBA.COMPANIA = CALIFICACION_PRUEBAS.COMPANIA 
  AND CLASES_DE_PRUEBA.PRUEBA  = CALIFICACION_PRUEBAS.PRUEBA    
WHERE APERTURA.COMPANIA                   = s$compania$s 
  AND APERTURA_INSCRITOS.NRO_CONVOCATORIA = s$convocatoria$s   
  AND RELACION_PRUEBAS.CONSECUTIVO        = (TRIM(s$consecutivo$s)) 
  AND CALIFICACION_PRUEBAS.CALIFICACION   >= RELACION_PRUEBAS.CALIFICACION
