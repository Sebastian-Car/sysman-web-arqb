SELECT 
  D.NUMEROCARPETA
 ,D.CODIGO
 ,D.NUMERO_DCTO
 ,(D.APELLIDO1 ||' '|| D.APELLIDO2 ||' '|| D.NOMBRES) NOMBREPERSONA
 ,E.FECHAACTO
 ,E.ACTO
 ,E.TIPOACTO
 ,E.ACTA
 ,E.FECHAACTA
 ,E.FECHAEFECTIVIDAD
 ,E.MOTIVOENCARGO
 ,E.DEPENDENCIA
 ,E.CARGO
 ,E.FECHAINICIO
 ,E.FECHAFINAL
 ,E.SUELDOMENSUAL
FROM NAT_DATOS_PERSONALES D
 INNER JOIN ENCARGOS E
    ON D.COMPANIA = E.COMPANIA
   AND D.CODIGO   = E.ID_DE_EMPLEADO
WHERE D.COMPANIA = s$compania$s
  AND D.NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s
