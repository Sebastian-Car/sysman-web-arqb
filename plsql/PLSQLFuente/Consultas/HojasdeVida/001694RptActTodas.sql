SELECT NAT_ACTIVIDADESPROGRAMADAS.IDEVENTO,
  NAT_ACTIVIDADESPROGRAMADAS.TIPOEVENTO,
  NAT_ACTIVIDADESPROGRAMADAS.FECHAINICIAL,
  NAT_ACTIVIDADESPROGRAMADAS.FECHAFINAL,
  NAT_ACTIVIDADESPROGRAMADAS.NUMEROHORAS,
  NAT_ACTIVIDADESPROGRAMADAS.NOMBREEVENTO,
  NAT_ACTIVIDADESPROGRAMADAS.VALOR,
  NAT_ACTIVIDADESPROGRAMADAS.ENTIDAD,
  NAT_ACTIVIDADESPROGRAMADAS.DIRECCIONEVENTO,
  NAT_ACTIVIDADESPROGRAMADAS.HORARIO,
  NAT_ACTIVIDADESPROGRAMADAS.OBSERVACIONES,
  NAT_ACTIVIDADESPROGRAMADAS.NUMEROCUPOS,
  NAT_ACTIVIDADESPROGRAMADAS.NUMERODOCUMENTO,
  NAT_ACTIVIDADESPROGRAMADAS.TIPODOCUMENTO,
  NAT_ACTIVIDADESPROGRAMADAS.NUMEROAPROBACIONCOMITE,
  NAT_ACTIVIDADESPROGRAMADAS.FECHAAPROBACIONCOMITE,
  NAT_ACTIVIDADESPROGRAMADAS.NIVELPROFESIONAL,
  NAT_ACTIVIDADESPROGRAMADAS.AREA,
  NAT_ENTIDADESCAPACITACION.NOMBREESTABLECIMIENTO
FROM NAT_ACTIVIDADESPROGRAMADAS
LEFT JOIN NAT_ENTIDADESCAPACITACION
	ON NAT_ACTIVIDADESPROGRAMADAS.COMPANIA            = NAT_ENTIDADESCAPACITACION.COMPANIA
	AND NAT_ACTIVIDADESPROGRAMADAS.NITESTABLECIMIENTO = NAT_ENTIDADESCAPACITACION.NITESTABLECIMIENTO
	AND NAT_ACTIVIDADESPROGRAMADAS.SUCURSAL           = NAT_ENTIDADESCAPACITACION.SUCURSAL
LEFT JOIN NAT_TIPOACTIVIDADES
	ON NAT_ACTIVIDADESPROGRAMADAS.COMPANIA = NAT_TIPOACTIVIDADES.COMPANIA
	AND NAT_ACTIVIDADESPROGRAMADAS.TIPOEVENTO = NAT_TIPOACTIVIDADES.CODIGO
WHERE NAT_ACTIVIDADESPROGRAMADAS.COMPANIA         = s$compania$s
  AND NAT_TIPOACTIVIDADES.MENU IN ('2104')
  AND NAT_ACTIVIDADESPROGRAMADAS.FECHAINICIAL BETWEEN s$fechaPrimera$s AND s$fechaFinal$s
ORDER BY NAT_ACTIVIDADESPROGRAMADAS.FECHAINICIAL
