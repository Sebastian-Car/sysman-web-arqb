SELECT DISTINCT NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA,
  NAT_APERTURA_INSCRITOS.NUMERO_DCTO,
  NAT_APERTURA_INSCRITOS.NOMBRE,
  CASE
    WHEN NAT_APERTURA_INSCRITOS.RECHAZOEXPERIENCIA NOT IN (0)
    THEN 'X'
    ELSE ''
  END RECHAZOEXPERIENCIA,
  CASE
    WHEN NAT_APERTURA_INSCRITOS.RECHAZOESTUDIOS NOT IN (0)
    THEN 'X'
    ELSE ''
  END RECHAZOESTUDIOS,
  CASE
    WHEN NAT_APERTURA_INSCRITOS.RECHAZOOTROS NOT IN (0)
    THEN 'X'
    ELSE ''
  END RECHAZOOTROS,
  NAT_APERTURA_INSCRITOS.OBSERVACIONES,
  NAT_APERTURA.FECHA_CONVOCATORIA,
  CARGOS.NOMBRE_DEL_CARGO AS DENOMINACION,
  NAT_APERTURA.FECHAINICIOINSCRIPCION,
  NAT_APERTURA.FECHACIERREINSCRIPCION,
  NAT_APERTURA.CODIGOCARGO,
  NAT_APERTURA.GRADOCARGO
FROM NAT_APERTURA
INNER JOIN NAT_APERTURA_INSCRITOS
        ON NAT_APERTURA.COMPANIA           = NAT_APERTURA_INSCRITOS.COMPANIA
       AND NAT_APERTURA.NRO_CONVOCATORIA   = NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA
LEFT JOIN CARGOS 	   
  ON NAT_APERTURA.COMPANIA = CARGOS.COMPANIA 		
  AND NAT_APERTURA.CODIGOCARGO = CARGOS.ID_DE_CARGO       
WHERE NAT_APERTURA_INSCRITOS.COMPANIA               = s$compania$s
  AND NAT_APERTURA_INSCRITOS.NRO_CONVOCATORIA       = 's$numeroConvocatoria$s' 
  AND (NAT_APERTURA_INSCRITOS.RECHAZOEXPERIENCIA    NOT IN (0)
          OR NAT_APERTURA_INSCRITOS.RECHAZOESTUDIOS     NOT IN (0)   		
          OR NAT_APERTURA_INSCRITOS.RECHAZOOTROS          NOT IN (0))
