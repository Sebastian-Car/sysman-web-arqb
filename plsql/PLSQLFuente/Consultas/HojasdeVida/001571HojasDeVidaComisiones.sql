SELECT 
  COMPANIA
 ,CO_NUMEDOCU
 ,CO_NUME
 ,CO_TIPODOCUACTO
 ,CO_DESC
 ,CO_FECHHASTA
 ,CO_FECHDESDE
 ,CO_TIPODOCU
 ,NVL(TO_CHAR(CO_FECHRESODECR,'DD/MM/YYYY'),' ') CO_FECHRESODECR
 ,CO_CARGO
 ,COPAIS CO_PAIS
 ,CODPTO CO_DPTO
 ,COMUNI CO_MUNI
 ,CO_ENTI
FROM NAT_COMISION
WHERE COMPANIA    = $P!{PR_COMPANIA}
  AND CO_NUMEDOCU = $P!{PR_CO_NUMEDOCU}
  AND SUCURSAL    = $P!{PR_SUCURSAL}
ORDER BY CO_NUMEDOCU DESC
