SELECT NUMEROCARPETA, 
       APELLIDO1, 
       APELLIDO2, 
       NOMBRES, 
       COMPANIA, 
       NUMERO_DCTO, 
       SUCURSAL
  FROM NAT_DATOS_PERSONALES
 WHERE COMPANIA = s$compania$s
   AND NUMEROCARPETA IS NOT NULL
   AND NUMEROCARPETA BETWEEN 's$carpetaInicial$s' AND 's$carpetaFinal$s' 
ORDER BY NUMEROCARPETA
