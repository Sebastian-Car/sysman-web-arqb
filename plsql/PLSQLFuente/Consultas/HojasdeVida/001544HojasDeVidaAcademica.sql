SELECT  
  F.NUMERO_DCTO
 ,UPPER(M.MO_NOMBRE) NOMMODALIDAD
 ,F.DIATERMINACION             || '/' || 
  LPAD(F.MESTERMINACION,2,'0') || '/' || 
  F.ANOTERMINACION         FECHATERMINACION
 ,F.NIVELAPROBADO
 ,F.TARJETAPROFESIONAL
 ,F.ESTABLECIMIENTO
 ,CASE WHEN F.GRADUADO NOT IN(0) 
       THEN 'X' 
  END                                            GRADUADO 
 ,PROFESIONES.NOMBRE_PROFESION TITULOOBTENIDO
FROM NAT_FORMACION_ACADEMICA F
  LEFT JOIN NAT_MODALIDAD_ESTUDIOSUPERIOR M
    ON F.MODALIDAD = M.MO_CODIGO
  LEFT JOIN PROFESIONES 
    ON F.TITULOOBTENIDO = PROFESIONES.CODIGOPROF
WHERE F.COMPANIA    = $P!{PR_COMPANIA} 
  AND F.NUMERO_DCTO = $P!{PR_NUMERO_DCTO} 
  AND F.SUCURSAL    = $P!{PR_SUCURSAL}   
ORDER BY 
  F.DIAINICIO DESC
 ,F.MESINICIO DESC 
 ,F.ANOINICIO DESC
