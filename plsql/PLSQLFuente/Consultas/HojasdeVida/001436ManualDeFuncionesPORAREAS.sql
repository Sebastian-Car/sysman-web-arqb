SELECT NAT_HVCARGOS.CODIGO,
  NAT_HVCARGOS.FECHACREACION,
  NAT_HVCARGOS.GRADO,
  NAT_HVCARGOS.DENOMINACION,
  NAT_CARGOS_POR_AREAS.CA_CODIGOAREA,
  NAT_CARGOS_POR_AREAS.CA_ANO,
  NAT_CARGOS_POR_AREAS.CA_RESPONSABILIDADES,
  NAT_CARGOS_POR_AREAS.CA_FECHA_CREACION,
  NAT_AREAS_DESEMPENO.NOMBRE_AREA,
  NAT_CARGOS_POR_AREAS.CA_ESTUDIOS,
  NAT_CARGOS_POR_AREAS.CA_EXPERIENCIA,
  NAT_CARGOS_POR_AREAS.CA_FORMACION,
  NAT_CARGOS_POR_AREAS.CA_HABILIDADES,
  NAT_CARGOS_POR_AREAS.SUPERIOR_JERARQUICO
FROM NAT_HVCARGOS
LEFT JOIN NAT_CARGOS_POR_AREAS
ON NAT_HVCARGOS.COMPANIA = NAT_CARGOS_POR_AREAS.COMPANIA
AND NAT_HVCARGOS.CODIGO  = NAT_CARGOS_POR_AREAS.CA_IDCARGO
AND NAT_HVCARGOS.GRADO   = NAT_CARGOS_POR_AREAS.GRADO
LEFT JOIN NAT_AREAS_DESEMPENO
ON NAT_CARGOS_POR_AREAS.CA_CODIGOAREA = NAT_AREAS_DESEMPENO.CODAREA
WHERE NAT_HVCARGOS.COMPANIA = s$compania$s
AND NAT_HVCARGOS.CODIGO = s$codigo$s
AND NAT_HVCARGOS.GRADO = s$grado$s
AND NAT_CARGOS_POR_AREAS.CA_CODIGOAREA = s$codigoarea$s
