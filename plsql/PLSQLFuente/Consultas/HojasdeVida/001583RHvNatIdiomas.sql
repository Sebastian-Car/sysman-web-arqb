SELECT 
	IDIOMA.NUMERO_DCTO, 
	IDIOMA.NUMERO, 
	IDIOMA.NOMBRE, 
	IDIOMA.HABLA, 
	IDIOMA.LEE, 
	IDIOMA.ESCRIBE, 
	IDIOMA.NOTAS, 
	PERSONA.APELLIDO1, 
	PERSONA.APELLIDO2, 
	PERSONA.NOMBRES
FROM NAT_IDIOMAS IDIOMA  
LEFT JOIN NAT_DATOS_PERSONALES PERSONA 
   ON IDIOMA.COMPANIA    = PERSONA.COMPANIA
  AND IDIOMA.NUMERO_DCTO = PERSONA.NUMERO_DCTO
  AND IDIOMA.SUCURSAL    = PERSONA.SUCURSAL 
WHERE IDIOMA.COMPANIA = s$compania$s 
  AND PERSONA.NUMERO_DCTO  BETWEEN 's$documentoPersonaInicial$s' AND 's$documentoPersonaFinal$s' 
ORDER BY PERSONA.NUMERO_DCTO
