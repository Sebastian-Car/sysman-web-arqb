SELECT 
  CESANTIAS.TIPO_CESANTIA
 ,CESANTIAS.NUMAUTO
 ,CESANTIAS.FECHA_AUTO
 ,FONDO.NOMBRE_FONDO_CESANTIAS
 ,CESANTIAS.DETALLE
 ,CESANTIAS.FECHA
 ,123 NUMEROFORMULARIO
 ,CESANTIAS.CESANTIA
FROM CESANTIAS
  LEFT JOIN V_FONDO_DE_CESANTIAS FONDO
    ON CESANTIAS.COMPANIA = FONDO.COMPANIA
   AND CESANTIAS.FONDO    = FONDO.FONDO_CESANTIAS
WHERE CESANTIAS.COMPANIA    = $P!{PR_COMPANIA} 
  AND CESANTIAS.NUMERO_DCTO = $P!{PR_NUMERO_DCTO} 
  AND CESANTIAS.SUCURSAL    = $P!{PR_SUCURSAL}
