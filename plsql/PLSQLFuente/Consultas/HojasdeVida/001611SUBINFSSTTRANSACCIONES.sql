SELECT DISTINCT   TRANSACCION.COMPANIA,
  TRANSACCION.CODIGO TIPO_TRANSACCION,
  TRANSACCION.CONSECUTIVODET CONSECUTIVO,
  SST_D_TRANSACCIONES.ID_EMPLEADO,
  SST_D_TRANSACCIONES.CEDULA,
  SST_D_TRANSACCIONES.SUCURSAL,
  SST_D_TRANSACCIONES.DESCRIPCION ,
   PERSONAL.NOMBRES || ' '|| PERSONAL.APELLIDO1 || ' '|| PERSONAL.APELLIDO2 NOMBRESP
FROM 
(s$consultaBase$s) TRANSACCION
LEFT JOIN SST_D_TRANSACCIONES
    ON   TRANSACCION.COMPANIA                 = SST_D_TRANSACCIONES.COMPANIA 
    AND  TRANSACCION.CODIGO                = SST_D_TRANSACCIONES.TIPO_TRANSACCION
    AND  TRANSACCION.CONSECUTIVO              = SST_D_TRANSACCIONES.CONSECUTIVO
INNER JOIN PERSONAL
    ON SST_D_TRANSACCIONES.COMPANIA                   = PERSONAL.COMPANIA
   AND SST_D_TRANSACCIONES.ID_EMPLEADO                = PERSONAL.ID_DE_EMPLEADO
   AND SST_D_TRANSACCIONES.CEDULA                     = PERSONAL.NUMERO_DCTO
   AND SST_D_TRANSACCIONES.SUCURSAL                   = PERSONAL.SUCURSAL   
WHERE TRANSACCION.COMPANIA          = $P!{PR_COMPANIA} 
  AND TRANSACCION.CODIGO                 = $P!{PR_TIPO_TRANSACCION} 
  AND TRANSACCION.CONSECUTIVO       = $P!{PR_CONSECUTIVO}
  AND TRANSACCION.CONSECUTIVODET IS NOT NULL
