SELECT NAT_DATOS_PERSONALES.NUMEROCARPETA,
  NAT_DATOS_PERSONALES.CODIGO,
  NAT_DATOS_PERSONALES.NUMERO_DCTO,
  NAT_DATOS_PERSONALES.APELLIDO1|| ' '|| NAT_DATOS_PERSONALES.APELLIDO2|| ' '|| NAT_DATOS_PERSONALES.NOMBRES AS NOMBRES,
  NAT_DATOS_PERSONALES.FECHAINGRESO,
  MAX(CASE WHEN NAT_QUINQUENIO.QU_FECHHASTA IS NULL THEN NAT_DATOS_PERSONALES.FECHAINGRESO ELSE NAT_QUINQUENIO.QU_FECHHASTA END )                                     AS DESDE,
  MAX(CASE WHEN NAT_QUINQUENIO.QU_FECHHASTA IS NULL THEN NAT_DATOS_PERSONALES.FECHAINGRESO + INTERVAL '5' YEAR ELSE NAT_QUINQUENIO.QU_FECHHASTA + INTERVAL '5' YEAR END ) AS HASTA,  
  MAX(NAT_QUINQUENIO.QU_FECHHASTA)                                                                                                            AS MÁXDEQU_FECHHASTA
FROM NAT_DATOS_PERSONALES
LEFT JOIN NAT_QUINQUENIO
ON NAT_DATOS_PERSONALES.COMPANIA = NAT_QUINQUENIO.COMPANIA
AND NAT_DATOS_PERSONALES.NUMERO_DCTO = NAT_QUINQUENIO.DP_NUMEDOCU
AND NAT_DATOS_PERSONALES.SUCURSAL  =NAT_QUINQUENIO.SUCURSAL
WHERE NAT_DATOS_PERSONALES.COMPANIA =  s$compania$s
AND NAT_DATOS_PERSONALES.NUMEROCARPETA BETWEEN 's$carpetaInicial$s' AND 's$carpetaFinal$s'
GROUP BY NAT_DATOS_PERSONALES.NUMEROCARPETA,
  NAT_DATOS_PERSONALES.CODIGO,
  NAT_DATOS_PERSONALES.NUMERO_DCTO,
  NAT_DATOS_PERSONALES.APELLIDO1||' '|| NAT_DATOS_PERSONALES.APELLIDO2|| ' '|| NAT_DATOS_PERSONALES.NOMBRES,
  NAT_DATOS_PERSONALES.FECHAINGRESO,
  FECHAINGRESO
HAVING  MIN(MONTHS_BETWEEN(SYSDATE,CASE WHEN NAT_QUINQUENIO.QU_FECHHASTA IS NULL THEN NAT_DATOS_PERSONALES.FECHAINGRESO ELSE NAT_QUINQUENIO.QU_FECHHASTA END )/12) <= 5
