WITH PAISDEPTOCIUDAD AS(
SELECT 
  P.PAIS
 ,D.CODIGO DEPARTAMENTO
 ,C.CODIGO CIUDAD
 ,P.NOMBRE NOMPAIS
 ,D.NOMBRE NOMDEPTO
 ,C.NOMBRE NOMCIUDAD
FROM PAISES P
  INNER JOIN DEPARTAMENTO D
    ON P.PAIS = D.PAIS
  INNER JOIN CIUDAD C
    ON D.CODIGO = C.DEPARTAMENTO
   AND D.PAIS   = C.PAIS
)
SELECT
  NCTO.NOMPAIS
 ,NCTO.NOMDEPTO
 ,NCTO.NOMCIUDAD
 ,RESIDE.NOMPAIS   RESPAIS
 ,RESIDE.NOMDEPTO  RESDEPT
 ,RESIDE.NOMCIUDAD RESCIUDAD
 ,EC.DESCRIPCION   ESTADO_CIVIL
 ,DATOS.NUMERO_DCTO
 ,DATOS.NOMBRES
 ,DATOS.APELLIDO1
 ,DATOS.APELLIDO2
 ,DATOS.NUMLIBMILITAR
 ,DATOS.NACIONALIDAD
 ,DATOS.FECHANCTO
 ,DATOS.DIRECCION
 ,DATOS.TELEFONOS
 ,DATOS.RH
 ,DATOS.NUMEROCARPETA
 ,DATOS.CODIGO
 ,DATOS.COMPANIA
 ,DATOS.SUCURSAL
 ,CASE WHEN DATOS.NACIONALIDAD   IN('C') 
       THEN 'X'
  END NACIONALIDAD_C
 ,CASE WHEN DATOS.NACIONALIDAD   IN('D') 
       THEN 'X'
  END NACIONALIDAD_D
 ,CASE WHEN DATOS.NACIONALIDAD   IN('E') 
       THEN 'X'
  END NACIONALIDAD_E  
 ,CASE WHEN DATOS.DCTO_IDENTIDAD IN('C')
       THEN 'X'
  END DCTO_C
 ,CASE WHEN DATOS.DCTO_IDENTIDAD IN('T')
       THEN 'X'
  END DCTO_T
 ,CASE WHEN DATOS.DCTO_IDENTIDAD IN('E')
       THEN 'X'
  END DCTO_E
 ,CASE WHEN DATOS.DCTO_IDENTIDAD IN('O')
       THEN 'X'
  END DCTO_O
 ,CASE WHEN DATOS.SEXO IN('F')
      THEN 'X'
  END GENERO_F
 ,CASE WHEN DATOS.SEXO IN('M')
      THEN 'X'
  END GENERO_M 
FROM NAT_DATOS_PERSONALES DATOS
  LEFT JOIN PAISDEPTOCIUDAD NCTO
    ON DATOS.PAISNCTO      = NCTO.PAIS
   AND DATOS.DEPTONCTO     = NCTO.DEPARTAMENTO
   AND DATOS.MUNICIPIONCTO = NCTO.CIUDAD
  LEFT JOIN PAISDEPTOCIUDAD RESIDE
    ON DATOS.PAISRESIDE      = RESIDE.PAIS
   AND DATOS.DEPTORESIDE     = RESIDE.DEPARTAMENTO
   AND DATOS.MUNICIPIORESIDE = RESIDE.CIUDAD  
  LEFT JOIN ESTADO_CIVIL EC
    ON DATOS.COMPANIA     = EC.COMPANIA
   AND DATOS.ESTADO_CIVIL = EC.ESTADO_CIVIL
WHERE DATOS.COMPANIA = s$compania$s
  AND DATOS.NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s 
  AND DATOS.NUMEROCARPETA IS NULL
  AND DATOS.FECHARETIRO IS NULL
ORDER BY DATOS.NUMERO_DCTO
