SELECT NPER.NUMEROCARPETA,
       NPER.NUMERO_DCTO,
       NPER.APELLIDO1 || ' ' || NPER.APELLIDO2 || ' ' || NPER.NOMBRES AS NOMBRECOMPLETO,
       FAM.IDENTIFICACION,
       PAR.DESCRIPCION PARENTESCO,
       (FAM.APELLIDO1 || ' ' || FAM.APELLIDO2 || ' ' || FAM.NOMBRE) AS NOMBREFAM ,
       FAM.FECHANCTO,
       (CASE WHEN FAM.SEXO = 'M' THEN 'Masculino' WHEN FAM.SEXO = 'F' THEN 'Femenino' END) SEXO,
       FAM.OCUPACION,
       FAM.TELEFONO,
       FAM.OBSERVACIONES,
       (CASE WHEN FAM.POLIZA NOT IN ('0') THEN 'X' ELSE '' END) POLIZA,
       (CASE WHEN FAM.SALUD NOT IN ('0') THEN 'X' ELSE '' END) SALUD,
       FAM.PORCENTAJE,
       (CASE WHEN FAM.POS NOT IN ('0') THEN 'X' ELSE '' END) POS,
       (CASE WHEN FAM.AUXILIO_EDUCATIVO NOT IN ('0') THEN 'X' ELSE '' END) AUXILIO_EDUCATIVO
,
       (CASE WHEN FAM.CAJAS_COMP NOT IN ('0') THEN 'X' ELSE '' END) CAJAS_COMP
FROM FAMILIARES FAM
INNER JOIN NAT_DATOS_PERSONALES NPER 
   ON FAM.COMPANIA = NPER.COMPANIA
  AND FAM.DCTO_EMPLEADO = NPER.NUMERO_DCTO
  AND FAM.SUCURSAL_EMPLEADO = NPER.SUCURSAL
INNER JOIN PARENTESCO PAR 
   ON PAR.COMPANIA = FAM.COMPANIA 
  AND PAR.PARENTESCO = FAM.PARENTESCO
WHERE FAM.COMPANIA = s$compania$s
AND NPER.NUMEROCARPETA BETWEEN s$carpetaInicial$s AND s$carpetaFinal$s
