SELECT NAT_INCENTIVOS.DP_NUMEDOCU,
  NAT_INCENTIVOS.IN_TIPODOCUACTO,
  NAT_INCENTIVOS.IN_NUME,
  NAT_INCENTIVOS.IN_FECHRESODECR,
  NAT_INCENTIVOS.IN_TIPOINCENT,
  NAT_INCENTIVOS.IN_CICLO,
  NAT_INCENTIVOS.IN_VALOR,
  NAT_INCENTIVOS.IN_COMPLEMENTO,
  NAT_DATOS_PERSONALES.APELLIDO1,
  NAT_DATOS_PERSONALES.APELLIDO2,
  NAT_DATOS_PERSONALES.NOMBRES,
  NAT_DATOS_PERSONALES.ID_CENTROS_DE_COSTO
FROM NAT_INCENTIVOS
LEFT JOIN NAT_DATOS_PERSONALES
	ON NAT_INCENTIVOS.COMPANIA     	= NAT_DATOS_PERSONALES.COMPANIA
	AND NAT_INCENTIVOS.DP_NUMEDOCU = NAT_DATOS_PERSONALES.NUMERO_DCTO
	AND NAT_INCENTIVOS.SUCURSAL 	= NAT_DATOS_PERSONALES.SUCURSAL
WHERE NAT_INCENTIVOS.COMPANIA = 's$compania$s'
	AND NAT_INCENTIVOS.IN_FECHRESODECR BETWEEN s$fechaInicial$s AND s$fechaFinal$s
	AND NAT_DATOS_PERSONALES.ID_CENTROS_DE_COSTO BETWEEN 's$centroInicial$s'AND 's$centroFinal$s'
	AND NAT_DATOS_PERSONALES.NUMERO_DCTO BETWEEN 's$empleadoInicial$s' AND 's$empleadoFinal$s'
