SELECT 
  DP.APELLIDO1 || ' ' || DP.APELLIDO2 || ' ' || DP.NOMBRES NOMBRECOMPLETO,
  DP.NUMERO_DCTO,
  HC.COMPANIA, 
  HC.FECHA_EXAMEN,
  HC.TIPO_EXAMEN,
  HC.VAL_AREA_TRABAJO,
  HC.ANTEC_PER_FAM,
  HC.ANTEC_TRABAJO,
  HC.ENFER_PROFES,
  HC.VACUNAS,
  HC.HABIT_TOXIC,
  HC.EXAMEN_FISICO,
  HC.ANTEC_GINECOBS,
  HC.DIAGN_CONDUCTA,
  HC.ANTEC_QUIRURG,
  HC.ANTEC_TRAUMATIC,
  HC.SECUELAS,
  HC.INMUNIZACIONES,
  HC.RECOMENDACIONES,
  HC.TRATAMIENTOS,
  HC.REMIS_MEDICAS,
  HC.HALLAZGOS,
  HC.ESTADO_MENTAL,
  HC.EVAL_AUIDIOMET,
  HC.EVAL_VISUAL,
  HC.EVAL_PARACLINICO,
  HC.APTITUDES,
  HC.EVAL_EGRESO,
  HC.CONCEPTO
FROM NAT_HISTORIA_CLINICA HC
INNER JOIN NAT_DATOS_PERSONALES DP 
   ON HC.COMPANIA    = DP.COMPANIA
  AND HC.NUMERO_DCTO = DP.NUMERO_DCTO 
  AND HC.SUCURSAL    = DP.SUCURSAL
WHERE HC.COMPANIA   = s$compania$s 
AND DP.NUMERO_DCTO = 's$noDocumentoEmpleado$s' 
