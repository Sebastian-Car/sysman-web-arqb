SELECT NAT_DATOS_PERSONALES.APELLIDO1
  ||' '
  ||NAT_DATOS_PERSONALES.APELLIDO2
  ||' '
  ||NAT_DATOS_PERSONALES.NOMBRES NOMBRES,
  LICENCIAS.FECHA_ACTO,
  LICENCIAS.FECHA_INICIO,
  LICENCIAS.FECHA_FINAL,
  LICENCIAS.OBSERVACION_L OBSERVACIONES,
  NAT_DATOS_PERSONALES.NUMEROCARPETA,
  NAT_DATOS_PERSONALES.NUMERO_DCTO  
FROM NAT_DATOS_PERSONALES
INNER JOIN PERSONAL
ON NAT_DATOS_PERSONALES.COMPANIA     = PERSONAL.COMPANIA
AND NAT_DATOS_PERSONALES.NUMERO_DCTO = PERSONAL.NUMERO_DCTO
INNER JOIN LICENCIAS
ON PERSONAL.COMPANIA                    = LICENCIAS.COMPANIA
AND PERSONAL.ID_DE_EMPLEADO             = LICENCIAS.ID_DE_EMPLEADO
WHERE LICENCIAS.COMPANIA=s$compania$s
AND LICENCIAS.FECHA_ACTO             IS NOT NULL
AND NAT_DATOS_PERSONALES.NUMEROCARPETA IS NOT NULL
AND NAT_DATOS_PERSONALES.NUMERO_DCTO BETWEEN 's$numeroInicial$s' AND 's$numeroFinal$s'
ORDER BY NAT_DATOS_PERSONALES.NUMERO_DCTO
