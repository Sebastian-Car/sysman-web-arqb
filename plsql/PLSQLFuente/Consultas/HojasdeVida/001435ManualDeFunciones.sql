SELECT 
    NAT_HVCARGOS.FECHACREACION, 
    NAT_HVCARGOS.CODIGO, 
    NAT_HVCARGOS.GRADO, 
    NAT_HVCARGOS.DENOMINACION, 
    NAT_HVCARGOS.NUMEROCARGOS, 
    NAT_HVCARGOS.DEPENDENCIA, 
    NAT_HVCARGOS.JEFEINMEDIATO, 
    NAT_HVCARGOS.NATURALEZA, 
    NAT_HVCARGOS.INDACTIVO, 
    COMPANIA.RUTA_IMAGEN LOGO, 
    COMPANIA.NOMBRE  NOMBRECOMPANIA, 
    NAT_HVCARGOS.PROPOSITO, 
    NAT_HVCARGOS.CONTRIBUCIONES_INDIVIDUALES, 
    NAT_HVCARGOS.CONOCIMIENTOS_BASICOS, 
    NAT_HVCARGOS.ESTUDIOS, 
    NAT_HVCARGOS.EXPERIENCIA, 
    NAT_HVCARGOS.EQUIVALENCIAS 
FROM 
    NAT_HVCARGOS
INNER JOIN
    COMPANIA
    ON NAT_HVCARGOS.COMPANIA = COMPANIA.CODIGO
WHERE COMPANIA = s$compania$s
  AND  NAT_HVCARGOS.CODIGO 			   = s$codigo$s
  AND  NAT_HVCARGOS.GRADO  			    = s$grado$s
