SELECT NAT_CARGOS_POR_AREAS.CA_FECHA_CREACION FECHACREACION,
  CARGOS.ID_DE_CARGO CODIGO,
  CARGOS.GRADO,
  CARGOS.NOMBRE_DEL_CARGO DENOMINACION,
  NAT_CARGOS_POR_AREAS.CA_CODIGOAREA,
  NAT_CARGOS_POR_AREAS.CA_ANO,
  NAT_CARGOS_POR_AREAS.CA_RESPONSABILIDADES,
  NAT_CARGOS_POR_AREAS.CA_FECHA_CREACION,
  NAT_AREAS_DESEMPENO.NOMBRE_AREA,
  NAT_CARGOS_POR_AREAS.CA_ESTUDIOS,
  NAT_CARGOS_POR_AREAS.CA_EXPERIENCIA,
  NAT_CARGOS_POR_AREAS.CA_FORMACION,
  NAT_CARGOS_POR_AREAS.CA_HABILIDADES,
  NAT_CARGOS_POR_AREAS.SUPERIOR_JERARQUICO
FROM CARGOS
INNER JOIN NAT_CARGOS_POR_AREAS
ON CARGOS.COMPANIA     = NAT_CARGOS_POR_AREAS.COMPANIA
AND CARGOS.ID_DE_CARGO = NAT_CARGOS_POR_AREAS.CA_IDCARGO
AND CARGOS.GRADO       = NAT_CARGOS_POR_AREAS.GRADO
INNER JOIN NAT_AREAS_DESEMPENO
ON NAT_CARGOS_POR_AREAS.COMPANIA       = NAT_AREAS_DESEMPENO.COMPANIA
AND NAT_CARGOS_POR_AREAS.CA_CODIGOAREA = NAT_AREAS_DESEMPENO.CODAREA
