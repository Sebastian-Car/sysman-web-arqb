SELECT 
  NSS.DP_NUMEDOCU,
  NSS.SS_REGIMEN,
  NSS.SS_FECHVINC,
  NSS.DP_TIPODOCU,
  NSS.SS_ENTIDAD,
  NSS.SS_NOVEDAD,
  NSS.SS_NRORADICACION,
  NSS.SS_FECHARADICACION,
  NVL(PERSONA.APELLIDO1,'  ') APELLIDO1,
  NVL(PERSONA.APELLIDO2,'  ') APELLIDO2,
  NVL(PERSONA.NOMBRES,'  ') NOMBRES,
  PERSONA.NUMEROCARPETA,
  FD.NOMBRE_FONDO_CESANTIAS
FROM NAT_SEGURIDAD_SOCIAL NSS
LEFT JOIN NAT_DATOS_PERSONALES PERSONA
      ON NSS.COMPANIA = PERSONA.COMPANIA
	  AND NSS.DP_NUMEDOCU = PERSONA.NUMERO_DCTO
	  AND NSS.SUCURSAL = PERSONA.SUCURSAL
LEFT JOIN V_FONDO_DE_CESANTIAS FD
	  ON  NSS.COMPANIA = FD.COMPANIA
	  AND NSS.SS_ENTIDAD = FD.FONDO_CESANTIAS
WHERE NSS.COMPANIA = s$compania$s
AND NSS.SS_REGIMEN= 'FONDO DE CESANTIAS'
AND NSS.DP_NUMEDOCU BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s
ORDER BY SS_FECHVINC
