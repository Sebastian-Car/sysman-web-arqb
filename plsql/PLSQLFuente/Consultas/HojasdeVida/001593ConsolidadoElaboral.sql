SELECT 
  PERSONAL.NUMEROCARPETA
 ,PERSONAL.CODIGO
 ,PERSONAL.NUMERO_DCTO
 ,(PERSONAL.APELLIDO1 ||' '|| PERSONAL.APELLIDO2 ||' '|| PERSONAL.NOMBRES)          NOMBREPERSONA
 ,LABORAL.NOMBRE
 ,DECODE(LABORAL.CLASE, '1', 'PÚBLICA', '2', 'PRIVADA', 'TRABAJADOR INDEPENDIENTE') CLASE
 ,PAIS.NOMBRE                                                                       PAIS
 ,DEPARTAMENTO.NOMBRE                                                               DEPTO
 ,CIUDAD.NOMBRE                                                                     MUNICIPIO
 ,LABORAL.DIRECCION
 ,LABORAL.TELEFONOS
 ,LABORAL.FECHAINGRESO
 ,LABORAL.FECHARETIRO
 ,LABORAL.ANOSERVICIO
 ,LABORAL.MESESERVICIO
 ,LABORAL.DIASERVICIO
 ,LABORAL.DEDICACION
 ,LABORAL.OTRADEDICACION
 ,LABORAL.CARGO
 ,LABORAL.DEPENDENCIA
 ,LABORAL.CAUSARETIRO
 ,LABORAL.ACTADEPOSESION
 ,LABORAL.EMAIL_EMPRESA
FROM NAT_DATOS_PERSONALES PERSONAL
  LEFT JOIN NAT_EXPERIENCIA_LABORAL LABORAL
    ON PERSONAL.COMPANIA    = LABORAL.COMPANIA
   AND PERSONAL.NUMERO_DCTO = LABORAL.NUMERO_DCTO
   AND PERSONAL.SUCURSAL    = LABORAL.SUCURSAL
  LEFT JOIN PAISES PAIS
    ON LABORAL.PAIS = PAIS.PAIS
  LEFT JOIN DEPARTAMENTO
    ON LABORAL.PAIS  = DEPARTAMENTO.PAIS
   AND LABORAL.DEPTO = DEPARTAMENTO.CODIGO
  LEFT JOIN CIUDAD
    ON LABORAL.PAIS      = CIUDAD.PAIS
   AND LABORAL.DEPTO     = CIUDAD.DEPARTAMENTO
   AND LABORAL.MUNICIPIO = CIUDAD.CODIGO  
WHERE PERSONAL.COMPANIA = s$compania$s
  AND PERSONAL.NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s
