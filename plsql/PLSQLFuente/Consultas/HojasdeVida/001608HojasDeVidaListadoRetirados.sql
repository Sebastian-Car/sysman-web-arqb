SELECT DISTINCT
  D.CODIGO
 ,D.NUMERO_DCTO
 ,D.APELLIDO1
 ,D.APELLIDO2
 ,D.NOMBRES
 ,D.FECHAINGRESO
 ,CANTERIOR.NOMBRE_DEL_CARGO NO_CARGOA
 ,CACTUAL.NOMBRE_DEL_CARGO   NO_CARGON
 ,N.NO_CAUSA
 ,D.FECHARETIRO
FROM NAT_DATOS_PERSONALES D
  INNER JOIN NAT_NOMBRAMIENTO N
     ON D.COMPANIA    = N.COMPANIA
    AND D.NUMERO_DCTO = N.DP_NUMEDOCU
   LEFT JOIN CARGOS CANTERIOR
     ON N.COMPANIA  = CANTERIOR.COMPANIA
    AND N.NO_CARGOA = CANTERIOR.ID_DE_CARGO
   LEFT JOIN CARGOS CACTUAL
     ON N.COMPANIA  = CACTUAL.COMPANIA
    AND N.NO_CARGON = CACTUAL.ID_DE_CARGO  
WHERE D.COMPANIA = s$compania$s
  AND D.NUMERO_DCTO BETWEEN s$empleadoInicial$s AND s$empleadoFinal$s   
  AND D.FECHARETIRO IS NULL
ORDER BY D.NUMERO_DCTO
