SELECT NAT_APERTURA.COMPANIA,
TO_CHAR(NAT_APERTURA.PUBLICACION,'DD/MM/YYYY') PUBLICACION,
  CARGOS.ID_DE_CARGO,
  CARGOS.GRADO,
  NAT_CLASES_DE_CONVOCATORIA.NOMBRE CLASE,
  NAT_APERTURA.CODIGOCARGO,
  CARGOS.DATE_CREATED,
  CARGOS.NOMBRE_DEL_CARGO DENOMINACION,
  NAT_APERTURA.NRO_CONVOCATORIA,
  TO_CHAR(NAT_APERTURA.FECHA_CONVOCATORIA,'DD/MM/YYYY') FECHA_CONVOCATORIA,
  NAT_APERTURA.PLAZAS,
  NAT_APERTURA.DENOMINACION,
  NAT_APERTURA.LUGAR_INSCRIPCION,
  TO_CHAR(NAT_APERTURA.FECHA_CONCURSO,'DD/MM/YYYY HH24:MI') FECHA_CONCURSO,
  TO_CHAR(NAT_APERTURA.FECHA_CONCURSO_FIN,'DD/MM/YYYY HH24:MI') FECHA_CONCURSO_FIN,
  NAT_APERTURA.LUGAR_CONCURSO,
  TO_CHAR(NAT_APERTURA.FECHA_CONCURSO,'HH24:MI')
  || ' a '
  || TO_CHAR(NAT_APERTURA.FECHA_CONCURSO_FIN,'HH24:MI') HORA_CONCURSO,
  NAT_APERTURA.SUELDO,
  NAT_APERTURA.REQUISITOEDUCACION,
  NAT_APERTURA.REQUISITOEXPERIENCIA,
  NAT_APERTURA.OBSERVACIONESREQUISITOS,
  NAT_APERTURA.REQUISITOEQUIVALENCIAS,
  NAT_APERTURA.REQUISITOEQUIEXPERIENCIA,
TO_CHAR(NAT_APERTURA.FECHAINICIOINSCRIPCION,'DD/MM/YYYY HH24:MI') FECHAINICIOINSCRIPCION,
  TO_CHAR(NAT_APERTURA.FECHACIERREINSCRIPCION,'DD/MM/YYYY HH24:MI') FECHACIERREINSCRIPCION,
  TO_CHAR(NAT_APERTURA.FECHAINICIOINSCRIPCION,'HH24:MI')
  || ' a '
  || TO_CHAR(NAT_APERTURA.FECHACIERREINSCRIPCION,'HH24:MI') HORA_ATENCION,
  DEPENDENCIA.NOMBRE UBICACIONORGANICA,
  NAT_APERTURA.UBICACIONORGANICA LUGARTRABAJO,
  NAT_APERTURA.NOTAS,
  NAT_APERTURA.GRADOCARGO
  ,EV_FUNCIONES.DESCRIPCION
FROM NAT_APERTURA
INNER JOIN CARGOS
ON NAT_APERTURA.COMPANIA     = CARGOS.COMPANIA
AND NAT_APERTURA.CODIGOCARGO = CARGOS.ID_DE_CARGO
AND NAT_APERTURA.GRADOCARGO  = CARGOS.GRADO
INNER JOIN NAT_CLASES_DE_CONVOCATORIA
ON NAT_CLASES_DE_CONVOCATORIA.CONVOCATORIA = NAT_APERTURA.CLASE
INNER JOIN DEPENDENCIA
ON NAT_APERTURA.COMPANIA     = DEPENDENCIA.COMPANIA
AND NAT_APERTURA.DEPENDENCIA = DEPENDENCIA.CODIGO
INNER JOIN EV_MANUAL
ON DEPENDENCIA.COMPANIA = EV_MANUAL.COMPANIA
AND DEPENDENCIA.CODIGO  = EV_MANUAL.DEPENDENCIA
AND CARGOS.ID_DE_CARGO  = EV_MANUAL.CARGO
INNER JOIN EV_FUNCIONES
ON EV_MANUAL.COMPANIA             = EV_FUNCIONES.COMPANIA
AND EV_MANUAL.NUMERO_MANUAL       = EV_FUNCIONES.NUMERO_MANUAL
AND EV_MANUAL.VERSION             = EV_FUNCIONES.VERSION
WHERE NAT_APERTURA.COMPANIA       = s$compania$s
AND NAT_APERTURA.NRO_CONVOCATORIA = s$convocatoria$s
