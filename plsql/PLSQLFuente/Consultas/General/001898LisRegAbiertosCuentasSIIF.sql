SELECT
    TO_CHAR(RUBRO) RUBRO,
    DESCRIPCION AS NOMBRE,
    NUMERO_DOCUMENTO AS COMPROBANTE,
    FUENTE AS AUXILIAR,
    FECHA_DE_REGISTRO AS FECHA,
    IDENTIFICACION AS CODTERCERO,
    '001' AS CODSUCURSAL,
    NOMBRE_RAZON_SOCIAL AS NOMBRETERCERO,
    NUMERO_DOCUMENTO_SOPORTE AS CONTRATO,
    OBSERVACIONES AS DESCR,
    VALOR_INICIAL AS MOVIMIENTO,
    ( VALOR_ACTUAL - SALDO_POR_UTILIZAR ) AS AFECTACION,
    VALOR_OPERACIONES AS MODIFICACION,
    SALDO_POR_UTILIZAR AS SALDO
FROM
    SIIF_REGISTRO_COMPROMISO
WHERE COMPANIA = s$compania$s
 AND  TO_NUMBER(TO_CHAR(FECHA_DE_REGISTRO,'YYYY')) = s$anio$s
 AND  TO_NUMBER(TO_CHAR(FECHA_DE_REGISTRO,'MM')) = s$mes$s
AND SALDO_POR_UTILIZAR<>0
ORDER BY RUBRO, NUMERO_DOCUMENTO
