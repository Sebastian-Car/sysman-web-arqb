SELECT   DISTINCT 
         CENTRO_COSTO.COMPANIA,  
         CENTRO_COSTO.CODIGO,  
         CENTRO_COSTO.NOMBRE,  
         CENTRO_COSTO.TIPO,  
         DECODE(CENTRO_COSTO.MOVIMIENTO,'-1','SI','NO') MOVIMIENTO ,
         CENTRO_COSTO.ANO
 FROM    CENTRO_COSTO  
 WHERE   CENTRO_COSTO.COMPANIA = 's$compania$s' 
 AND     CENTRO_COSTO.CODIGO  BETWEEN  's$codigoInicial$s' 
 AND  's$codigoFinal$s'
