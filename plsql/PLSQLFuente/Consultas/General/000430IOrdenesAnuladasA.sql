SELECT  
	ORDENDECOMPRA.FECHAANULACION, 
	ORDENDECOMPRA.FECHA, 
       ORDENDECOMPRA.CLASEORDEN ||  '-' ||  ORDENDECOMPRA.NUMERO AS NUMEROORDEN, 
       ORDENDECOMPRAPPTO.NUMEROPPTO AS DISPONIBILIDAD, 
       ORDENDECOMPRAPPTO.RUBRO, 
       ORDENDECOMPRA.OBJETOCONTRATO, 
       ORDENDECOMPRA.VALORTOTAL, 
       TERCERO.NIT || '-'||  TERCERO.DIGITOVERIFICACION  ||'  '||  TERCERO.NOMBRE AS PROPONENTE 
FROM ORDENDECOMPRA  
  LEFT JOIN TERCERO  
    ON ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA  
    AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL  
    AND ORDENDECOMPRA.TERCERO = TERCERO.NIT  
  LEFT JOIN TIPOORDENDECOMPRA  
    ON ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA 
    AND ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO  
   LEFT JOIN ORDENDECOMPRAPPTO 
    ON ORDENDECOMPRA.COMPANIA = ORDENDECOMPRAPPTO.COMPANIA 
    AND ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRAPPTO.CLASEORDEN 
    AND ORDENDECOMPRA.NUMERO = ORDENDECOMPRAPPTO.NUMERO 
  WHERE ORDENDECOMPRA.COMPANIA =  s$compania$s   
    AND ORDENDECOMPRA.FECHA BETWEEN  s$fechaInicial$s AND  s$fechaFinal$s 
ORDER BY ORDENDECOMPRA.COMPANIA, ORDENDECOMPRA.CLASEORDEN, ORDENDECOMPRA.NUMERO
