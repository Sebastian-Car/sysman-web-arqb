SELECT D_ORDENDECOMPRA.ELEMENTO,  
    SUM(D_ORDENDECOMPRA.CANTIDAD)  SUMADECANTIDAD,  
    INVENTARIO.CUBS,  
    SUM(D_ORDENDECOMPRA.VLRTOTAL)  SUMADEVLRTOTAL,  
    INVENTARIO.NOMBRELARGO,   
    MIN(ORDENDECOMPRAPPTO.RUBRO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) PRIMERODERUBRO,   
    ORDENDECOMPRA.NUMERO,   
    D_ORDENDECOMPRA.ORDENDECOMPRA,  
    TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY') ANO,  
    D_ORDENDECOMPRA.VALORUNITARIO,  
    ORDENDECOMPRA.SUCURSAL,  
    ORDENDECOMPRA.DEPENDENCIA,  
    ORDENDECOMPRA.FECHA,  
    ORDENDECOMPRA.PORCIVAGLOBAL  
FROM  ((D_ORDENDECOMPRA LEFT JOIN INVENTARIO   
          ON D_ORDENDECOMPRA.COMPANIA = INVENTARIO.COMPANIA   
              AND D_ORDENDECOMPRA.ELEMENTO = INVENTARIO.CODIGOELEMENTO )   
      LEFT JOIN ORDENDECOMPRA   
          ON D_ORDENDECOMPRA.COMPANIA = ORDENDECOMPRA.COMPANIA   
              AND D_ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRA.CLASEORDEN   
              AND D_ORDENDECOMPRA.ORDENDECOMPRA = ORDENDECOMPRA.NUMERO)  
      LEFT JOIN ORDENDECOMPRAPPTO  
          ON D_ORDENDECOMPRA.COMPANIA = ORDENDECOMPRAPPTO.COMPANIA   
              AND D_ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRAPPTO.CLASEORDEN   
              AND D_ORDENDECOMPRA.ORDENDECOMPRA = ORDENDECOMPRAPPTO.NUMERO  
WHERE D_ORDENDECOMPRA.COMPANIA= s$compania$s  
    AND D_ORDENDECOMPRA.ELEMENTO BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s'  
    AND ORDENDECOMPRA.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s  
GROUP BY  D_ORDENDECOMPRA.ELEMENTO,   
          INVENTARIO.CUBS,   
          INVENTARIO.NOMBRELARGO,   
          ORDENDECOMPRA.NUMERO,   
          D_ORDENDECOMPRA.ORDENDECOMPRA,   
          TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY'),   
          D_ORDENDECOMPRA.VALORUNITARIO,   
          ORDENDECOMPRA.SUCURSAL,   
          ORDENDECOMPRA.DEPENDENCIA,   
          ORDENDECOMPRA.FECHA,   
          ORDENDECOMPRA.PORCIVAGLOBAL  
ORDER BY ORDENDECOMPRA.NUMERO
