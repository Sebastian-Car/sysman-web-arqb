SELECT
    SIIF_OBLIGACIONES.COMPANIA,
    TO_CHAR(SIIF_OBLIGACIONES.NUMERO_DOCUMENTO) AS COMPROBANTE,
    SIIF_OBLIGACIONES.FECHA_DE_REGISTRO AS FECHA,
    SIIF_OBLIGACIONES.RUBRO,
    SIIF_OBLIGACIONES.DESCRIPCION,
    SIIF_OBLIGACIONES.IDENTIFICACION AS CODTERCERO,
    SIIF_OBLIGACIONES.NOMBRE_RAZON_SOCIAL AS NOMBRETERCERO,
    SIIF_OBLIGACIONES.VALOR_INICIAL AS MOVIMIENTO,
    ( VALORACTUAL - SALDO_POR_UTILIZAR ) AS AFECTACION,
    SIIF_OBLIGACIONES.VALOR_OPERACIONES AS MODIFICACION,
    SIIF_OBLIGACIONES.SALDO_POR_UTILIZAR AS SALDO
FROM
    SIIF_OBLIGACIONES
WHERE COMPANIA = s$compania$s
 AND  TO_NUMBER(TO_CHAR(FECHA_DE_REGISTRO,'YYYY')) = s$anio$s
 AND  TO_NUMBER(TO_CHAR(FECHA_DE_REGISTRO,'MM')) = s$mes$s
ORDER BY SIIF_OBLIGACIONES.NUMERO_DOCUMENTO,
         SIIF_OBLIGACIONES.RUBRO 
