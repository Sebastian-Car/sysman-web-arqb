ON SF_FACTURA.COMPANIA=TERCERO.COMPANIA
AND SF_FACTURA.TERCERO=TERCERO.NIT
AND SF_FACTURA.SUCURSAL=TERCERO.SUCURSAL
WHERE SF_FACTURA.COMPANIA='s$compania$s'
AND SF_FACTURA.TIPO_FACTURA= 's$tipoCobro$s'
AND SF_FACTURA.TERCERO BETWEEN 's$nitInicial$s' AND 's$nitFinal$s'
AND TRUNC(SF_FACTURA.FECHA_EXPEDICION) BETWEEN TRUNC(s$fechaIncial$s) AND TRUNC(s$fechaFinal$s)
GROUP BY(SF_FACTURA.TIPO_FACTURA,SF_FACTURA.NUMERO_FACTURA,
SF_FACTURA.FECHA_EXPEDICION, SF_FACTURA.VALOR_TOTAL, TERCERO.NOMBRE,SF_DETALLE_FACTURA.TERCERO) order by SF_FACTURA.NUMERO_FACTURA,SF_FACTURA.FECHA_EXPEDICION,NOMBRE]') CONSULTA, 69 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);