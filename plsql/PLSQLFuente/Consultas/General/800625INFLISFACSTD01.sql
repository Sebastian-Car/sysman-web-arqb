    SF_FACTURA.NUMERO_FACTURA FACTURA,
    TO_CHAR(SF_FACTURA.FECHA_EXPEDICION,'DD/MM/YYYY') FECHA,
    SF_DETALLE_FACTURA.TERCERO NIT,
    TERCERO.NOMBRE NOMBRE,
    SUM(SF_DETALLE_FACTURA.VALOR_BASE) VALOR_VENTA,
    SUM(SF_DETALLE_FACTURA.VALOR_NETO) VALOR_NETO,
    SUM(SF_DETALLE_FACTURA.VALOR_IVA) VALOR_IVA,
    SF_FACTURA.VALOR_TOTAL TOTAL
