SELECT *
FROM(
SELECT SYSMANEJECUCION.CODIGO RUBRO
,SYSMANEJECUCION.NOMBRE DESCRIPCION
,SYSMANEJECUCION.SOLICITUD_CDP
,SYSMANEJECUCION.VLR_SOLICITUD
,SYSMANEJECUCION.NUMERO_DOCUMENTO
,SYSMANEJECUCION.VLR_DISPONIBILIDAD
,SYSMANEJECUCION.INCREMENTO_CDP
,SYSMANEJECUCION.DECREMENTO_CDP
,SYSMANEJECUCION.ANULACION
,NVL(SYSMANEJECUCION.VALOR_FINAL_DISPONIBILIDAD,0) VALOR_FINAL_DISPONIBILIDAD
,SYSMANEJECUCION.FECHA_REGISTRO
,SYSMANEJECUCION.NUMERO_REGISTRO
,NVL(SYSMANEJECUCION.OBSERVACIONES,' ') OBSERVACIONES
,NVL(SYSMANEJECUCION.IDENTIFICACION,' ') IDENTIFICACION
,NVL(SYSMANEJECUCION.VLR_INICIAL_REGISTRO,0) VLR_INICIAL_REGISTRO
,SYSMANEJECUCION.INCREMENTO_CRP
,SYSMANEJECUCION.DECREMENTO_CRP
,SYSMANEJECUCION.ANULACION_REGISTRO
,NVL(SYSMANEJECUCION.VALOR_FINAL_REGISTRO,0) VALOR_FINAL_REGISTRO
,NVL(SYSMANEJECUCION.SALDO_DEL_CDP,0) SALDO_DEL_CDP 
,NVL(OBLIGACIONES.VALOR_FINAL_OBLIGACIONES,0) VALOR_FINAL_OBLIGACIONES
,NVL(SYSMANEJECUCION.VALOR_FINAL_REGISTRO-OBLIGACIONES.VALOR_FINAL_OBLIGACIONES,0) SALDO_DEL_CRP
,NVL(ORDENDEPAGO.PAGOS,0) PAGOS
,SYSMANEJECUCION.DEPENDENCIA_DESCRIPCION
FROM( 
SELECT DISTINCT PLAN_PRESUPUESTAL.ANO,PLAN_PRESUPUESTAL.CODIGO,
PLAN_PRESUPUESTAL.NOMBRE,
SIIF_DISPONIBILIDADES.SOLICITUD_CDP,
SIIF_DISPONIBILIDADES.VALOR_INICIAL VLR_SOLICITUD,
SIIF_DISPONIBILIDADES.NUMERO_DOCUMENTO,
SIIF_DISPONIBILIDADES.VALOR_INICIAL VLR_DISPONIBILIDAD,
CASE WHEN SIIF_DISPONIBILIDADES.VALOR_OPERACIONES >0 THEN SIIF_DISPONIBILIDADES.VALOR_OPERACIONES ELSE 0 END AS INCREMENTO_CDP,  
CASE WHEN SIIF_DISPONIBILIDADES.VALOR_OPERACIONES <0 THEN SIIF_DISPONIBILIDADES.VALOR_OPERACIONES ELSE 0 END AS DECREMENTO_CDP,
CASE WHEN SIIF_DISPONIBILIDADES.VALOR_INICIAL+SIIF_DISPONIBILIDADES.VALOR_OPERACIONES=0 THEN SIIF_DISPONIBILIDADES.VALOR_OPERACIONES ELSE 0 END  AS ANULACION,
SIIF_DISPONIBILIDADES.VALOR_ACTUAL AS VALOR_FINAL_DISPONIBILIDAD,
SIIF_REGISTRO_COMPROMISO.FECHA_DE_CREACION FECHA_REGISTRO,
NVL(SIIF_REGISTRO_COMPROMISO.NUMERO_DOCUMENTO,0) NUMERO_REGISTRO,
NVL(SIIF_REGISTRO_COMPROMISO.OBSERVACIONES,' ')OBSERVACIONES,
SIIF_REGISTRO_COMPROMISO.IDENTIFICACION,
SIIF_REGISTRO_COMPROMISO.VALOR_INICIAL VLR_INICIAL_REGISTRO,
CASE WHEN SIIF_REGISTRO_COMPROMISO.VALOR_OPERACIONES >0 THEN SIIF_REGISTRO_COMPROMISO.VALOR_OPERACIONES ELSE 0 END AS INCREMENTO_CRP,  
CASE WHEN SIIF_REGISTRO_COMPROMISO.VALOR_OPERACIONES <0 THEN SIIF_REGISTRO_COMPROMISO.VALOR_OPERACIONES ELSE 0 END AS DECREMENTO_CRP,
CASE WHEN SIIF_REGISTRO_COMPROMISO.VALOR_INICIAL+SIIF_REGISTRO_COMPROMISO.VALOR_OPERACIONES=0 THEN SIIF_REGISTRO_COMPROMISO.VALOR_OPERACIONES ELSE 0 END  AS ANULACION_REGISTRO,
SIIF_REGISTRO_COMPROMISO.VALOR_ACTUAL AS VALOR_FINAL_REGISTRO,
SIIF_DISPONIBILIDADES.VALOR_ACTUAL-SIIF_REGISTRO_COMPROMISO.VALOR_ACTUAL SALDO_DEL_CDP,
SIIF_DISPONIBILIDADES.DEPENDENCIA_DESCRIPCION
FROM PLAN_PRESUPUESTAL 
LEFT JOIN SIIF_DISPONIBILIDADES
   ON PLAN_PRESUPUESTAL.COMPANIA=SIIF_DISPONIBILIDADES.COMPANIA 
   AND PLAN_PRESUPUESTAL.ANO=TO_NUMBER(TO_CHAR(SIIF_DISPONIBILIDADES.FECHA_DE_REGISTRO,'YYYY'))
   AND REPLACE(PLAN_PRESUPUESTAL.CODIGO,' ','')=SIIF_DISPONIBILIDADES.RUBRO
LEFT JOIN SIIF_REGISTRO_COMPROMISO
   ON SIIF_DISPONIBILIDADES.RUBRO=SIIF_REGISTRO_COMPROMISO.RUBRO
   AND SIIF_DISPONIBILIDADES.NUMERO_DOCUMENTO=SIIF_REGISTRO_COMPROMISO.CDP
LEFT JOIN SALDO_AUX_PPTAL
   ON PLAN_PRESUPUESTAL.COMPANIA=SALDO_AUX_PPTAL.COMPANIA 
   AND PLAN_PRESUPUESTAL.ANO=SALDO_AUX_PPTAL.ANO
   AND REPLACE(PLAN_PRESUPUESTAL.CODIGO,' ','')=REPLACE(SALDO_AUX_PPTAL.CODIGO,' ','')   
  WHERE PLAN_PRESUPUESTAL.COMPANIA=s$compania$s
   AND PLAN_PRESUPUESTAL.ANO=s$anio$s
   AND SALDO_AUX_PPTAL.MES<= s$mes$s
   AND SIIF_DISPONIBILIDADES.SOLICITUD_CDP IS NOT NULL
ORDER BY PLAN_PRESUPUESTAL.CODIGO)  SYSMANEJECUCION
LEFT JOIN 
  (SELECT RUBRO,COMPROMISOS,SUM(VALOR_ACTUAL) VALOR_FINAL_OBLIGACIONES
  FROM SIIF_OBLIGACIONES
  WHERE ESTADO NOT IN('Anulada')
  GROUP BY RUBRO,COMPROMISOS) OBLIGACIONES
  ON REPLACE(SYSMANEJECUCION.CODIGO,' ','')=OBLIGACIONES.RUBRO
  AND SYSMANEJECUCION.NUMERO_REGISTRO=OBLIGACIONES.COMPROMISOS         
LEFT JOIN (
  SELECT RUBRO,COMPROMISOS,SUM(VALOR_PESOS-VALOR_REINTEGRADO_PESOS) PAGOS
  FROM SIIF_ORDENESPAGO
  WHERE ESTADO NOT IN('Anulada')
  GROUP BY RUBRO,COMPROMISOS) ORDENDEPAGO 
              ON ORDENDEPAGO.RUBRO=OBLIGACIONES.RUBRO
              AND ORDENDEPAGO.COMPROMISOS=OBLIGACIONES.COMPROMISOS 
              ORDER BY SYSMANEJECUCION.CODIGO)
