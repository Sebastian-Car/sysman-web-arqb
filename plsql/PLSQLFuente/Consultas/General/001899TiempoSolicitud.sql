SELECT TO_CHAR(SOLICITUDDISPONIBILIDAD.NUMERO) NUMERO, SOLICITUDDISPONIBILIDAD.FECHA, TERCERO.NOMBRE, D_SOLICITUDDISPONIBILIDAD.VALOR, 
CASE WHEN SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD =1 THEN 'X' ELSE '' END AS NUEVO, 
CASE WHEN SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD=2 THEN 'X' ELSE '' END AS INCREMENTO, 
CASE WHEN SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD=3 THEN 'X' ELSE '' END AS DECREMENTO, 
0 AS ANULACION, 
FECHA_APROBACION+3 AS FECHA_APROBACION1, 
SIIF_DISPONIBILIDADES.SOLICITUD_CDP AS CDP, SIIF_DISPONIBILIDADES.FECHA_DE_CREACION, 
TRUNC(FECHA)-TRUNC(FECHA_DE_CREACION) AS TIEMPO_RESPUESTA
FROM TERCERO 
INNER JOIN SOLICITUDDISPONIBILIDAD 
INNER JOIN D_SOLICITUDDISPONIBILIDAD 
INNER JOIN SIIF_DISPONIBILIDADES 
ON   D_SOLICITUDDISPONIBILIDAD.COMPANIA = SIIF_DISPONIBILIDADES.COMPANIA 
AND  D_SOLICITUDDISPONIBILIDAD.NUMERO = SIIF_DISPONIBILIDADES.SOLICITUD_CDP 
ON   SOLICITUDDISPONIBILIDAD.ANO = D_SOLICITUDDISPONIBILIDAD.ANO 
AND  SOLICITUDDISPONIBILIDAD.NUMERO = D_SOLICITUDDISPONIBILIDAD.NUMERO 
AND  SOLICITUDDISPONIBILIDAD.COMPANIA = D_SOLICITUDDISPONIBILIDAD.COMPANIA 
ON   TERCERO.SUCURSAL = SOLICITUDDISPONIBILIDAD.SUCURSAL 
AND  TERCERO.NIT = SOLICITUDDISPONIBILIDAD.TERCERO 
AND  TERCERO.COMPANIA = SOLICITUDDISPONIBILIDAD.COMPANIA
WHERE SIIF_DISPONIBILIDADES.COMPANIA=s$compania$s
 AND  TO_NUMBER(TO_CHAR(FECHA_DE_CREACION,'YYYY')) = s$anio$s
 AND  TO_NUMBER(TO_CHAR(FECHA_DE_CREACION,'MM')) = s$mes$s
