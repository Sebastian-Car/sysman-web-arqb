SELECT CENTRO_COSTO.CODIGO,  
      CENTRO_COSTO.NOMBRE,  
      CENTRO_COSTO.TIPO,  
      CASE CENTRO_COSTO.MOVIMIENTO WHEN 0 THEN  ' ' WHEN -1 THEN 'x' END MOVIMIENTO 
FROM CENTRO_COSTO  
WHERE CENTRO_COSTO.COMPANIA = s$compania$s 
  AND CENTRO_COSTO.ANO 		  = s$anioNomina$s 
ORDER BY  CENTRO_COSTO.CODIGO
