SELECT COMPROBANTE.COMPANIA,
       COMPROBANTE.TIPO, 
       MIN(COMPROBANTE.NUMERO) AS PRIMERO, 
       MAX(COMPROBANTE.NUMERO) AS ULTIMO, 
       TIPOCOM.NOMBRE, 
       COUNT(COMPROBANTE.NUMERO) AS NUMREG, 
       s$fconsecutivos$s(COMPROBANTE.TIPO, COMPROBANTE.COMPANIA, 's$fechaInicial$s', 's$fechaFinal$s') AS NOEXISTENTES 
 FROM s$tablacomprobante$s COMPROBANTE
 LEFT JOIN s$tablatipocom$s TIPOCOM 
  ON COMPROBANTE.COMPANIA = TIPOCOM.COMPANIA
  AND COMPROBANTE.TIPO = TIPOCOM.CODIGO
 WHERE COMPROBANTE.COMPANIA = s$compania$s
 AND COMPROBANTE.FECHA BETWEEN 's$fechaInicial$s' AND 's$fechaFinal$s' 
 GROUP BY COMPROBANTE.COMPANIA, COMPROBANTE.TIPO, TIPOCOM.NOMBRE 
 ORDER BY COMPROBANTE.COMPANIA, COMPROBANTE.TIPO, MIN(COMPROBANTE.NUMERO)
