WITH QRY_ULTIMA_MOD_ESTRATO AS
  (SELECT AUDITORIA.COMPANIA, 
          TABLA_ESTRATOS_1.CODIGO_ESTRATO ESTRATO_NU,
          TABLA_ESTRATOS.NOMBRE_ESTRATO ESTRATO_ANT,
          AUDITORIA.MODCOD,
          MAX(AUDITORIA.MODFEC) KEEP (DENSE_RANK LAST ORDER BY ROWNUM)  ULTIMODEMODFEC,
          MAX(AUDITORIA.MODHOR) KEEP (DENSE_RANK LAST  ORDER BY ROWNUM) ULTIMODEMODHOR
  FROM IP_USUARIOS_PREDIAL USUARIOS_PREDIAL
   LEFT JOIN IP_AUDITORIA AUDITORIA
    ON  USUARIOS_PREDIAL.COMPANIA               = AUDITORIA.COMPANIA
    AND USUARIOS_PREDIAL.ESTRATO_SOCIOECONOMICO = AUDITORIA.MODVNU
    AND USUARIOS_PREDIAL.CODIGO                 = AUDITORIA.MODCOD
  LEFT JOIN IP_TABLA_ESTRATOS TABLA_ESTRATOS
  ON AUDITORIA.MODVAN = TABLA_ESTRATOS.CODIGO_ESTRATO
  LEFT JOIN IP_TABLA_ESTRATOS TABLA_ESTRATOS_1
  ON AUDITORIA.MODVNU      = TABLA_ESTRATOS_1.CODIGO_ESTRATO
  WHERE AUDITORIA.COMPANIA = s$compania$s
    AND AUDITORIA.MODCCO   = '550'
  GROUP BY AUDITORIA.COMPANIA, 
           TABLA_ESTRATOS_1.CODIGO_ESTRATO,
           TABLA_ESTRATOS.NOMBRE_ESTRATO,
           AUDITORIA.MODCOD
  )
SELECT SUBSTR(CODIGO,1,2) TIPOP,
       SUBSTR(CODIGO,3,2)      SECTOR,
       SUBSTR(CODIGO,5,4)      MANZANA,
       SUBSTR(CODIGO,9,4)      PREDIO,
       SUBSTR(CODIGO,13,3)     MEJORA,
       QRY_ULTIMA_MOD_ESTRATO.ESTRATO_ANT,
       USUARIOS_PREDIAL.DESTINO_ECONOMICO,
       TABLA_ESTRATOS.NOMBRE_ESTRATO ESTRATO_SOCIOECON,
       FORMATO_CERTIFICADOS.NOMBRE   FORMATO_CERTIFICADO,
       USUARIOS_PREDIAL.NIT          IDENTIFICACION,
       USUARIOS_PREDIAL.DIRECCION,
       USUARIOS_PREDIAL.NOMBRE      PROPIETARIO,
       CASE WHEN PERMITE_EXP_CERT NOT IN (0)
            THEN 'SI'
            ELSE 'NO'
            END ACTIVO
,
      USUARIOS_PREDIAL.MUNICIPIO 
FROM IP_USUARIOS_PREDIAL USUARIOS_PREDIAL
 INNER JOIN IP_TABLA_ESTRATOS TABLA_ESTRATOS
  ON USUARIOS_PREDIAL.ESTRATO_SOCIOECONOMICO = TABLA_ESTRATOS.CODIGO_ESTRATO
 LEFT JOIN IP_FORMATO_CERTIFICADOS FORMATO_CERTIFICADOS
  ON USUARIOS_PREDIAL.FORMATO_ESTRATO = FORMATO_CERTIFICADOS.FORMATO
 INNER JOIN QRY_ULTIMA_MOD_ESTRATO
  ON  USUARIOS_PREDIAL.COMPANIA = QRY_ULTIMA_MOD_ESTRATO.COMPANIA
  AND USUARIOS_PREDIAL.CODIGO   = QRY_ULTIMA_MOD_ESTRATO.MODCOD
WHERE USUARIOS_PREDIAL.NUMERO_ORDEN  = s$numeroOrden$s
  AND USUARIOS_PREDIAL.COMPANIA      = s$compania$s
  AND USUARIOS_PREDIAL.CODIGO        BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
  s$condEstado$s
GROUP BY SUBSTR(CODIGO,1,2),
         SUBSTR(CODIGO,3,2),
         SUBSTR(CODIGO,5,4),
         SUBSTR(CODIGO,9,4),
         SUBSTR(CODIGO,13,3),
         USUARIOS_PREDIAL.DESTINO_ECONOMICO,
         TABLA_ESTRATOS.NOMBRE_ESTRATO,
         FORMATO_CERTIFICADOS.NOMBRE,
         QRY_ULTIMA_MOD_ESTRATO.ESTRATO_ANT,
         USUARIOS_PREDIAL.NIT,
         USUARIOS_PREDIAL.DIRECCION,
         USUARIOS_PREDIAL.NOMBRE,
         CASE WHEN PERMITE_EXP_CERT NOT IN (0)
              THEN 'SI'
              ELSE 'NO'
              END,
         USUARIOS_PREDIAL.CODIGO,
         USUARIOS_PREDIAL.PERMITE_EXP_CERT,
         USUARIOS_PREDIAL.NUMERO_ORDEN,
      USUARIOS_PREDIAL.MUNICIPIO 
