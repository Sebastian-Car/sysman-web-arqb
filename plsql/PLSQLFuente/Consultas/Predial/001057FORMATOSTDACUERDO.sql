SELECT DISTINCT RP.ACUERDO,
  UP.CODIGO,
  UP.NIT,
  UP.AREA_HA,
  UP.AREA_M2,
  UP.AREA_CONSTRUIDA,
  RP.NCUOTA_ACUERDO,
  RP.C1+RP.C5+RP.C9  AS IMP,
  RP.C2+RP.C6+RP.C10 AS INT,
  RP.C3+RP.C7+RP.C11 AS CAR,
  RP.C4+RP.C8+RP.C12 AS INTCAR,
  RP.C13,
  RP.C14,
  RP.C15,
  RP.C16,
  RP.C17,
  RP.C18,
  RP.C19,
  RP.C20,
  RP.PREVAL,
  RP.PREANOI,
  RP.PREANO,
  UP.NOMBRE,
  UP.DIRECCION,
  RP.DOCNUM,
  RP.PREFECLIM,
  TO_CHAR(RP.FECHAFACACUERDO,'DD/MM/YYYY') AS FECHAFACACUERDO,
  DR.INTERES_ACUERDO AS INTA,
  DR.INTERES_RECARGO AS INTR,
  RP.PREANO AS ANOFIN,
  RP.PREANOI AS ANOINI,
  UP.DIRECCION_CORRESPONDENCIA,
  UP.NUMERO_ORDEN,
  UP.TRPCOD,
  UP.ESTRATO_SOCIOECONOMICO,
  UP.NUM_COM,
  UP.PAG_VAL,
  TO_CHAR(UP.PAG_FEC,'DD/MM/YY') PAG_FEC,
  DR.INTERES_ACUERDO + DR.INTERES_RECARGO AS INTERESACUERDO,
  UP.COD_POSTAL,
  UP.CODIGO_EQUIVALENTE,
  PCK_PREDIAL.FC_CUOTASRECIBOS(UP.COMPANIA,RP.ACUERDO,UP.CODIGO,RP.DOCNUM) AS CUOTASRECIBOS,
  SUBSTR(CHR(205)  ||CHR(102)  ||CHR(40)  ||'415'  ||CHR(41)  ||s$strCodigoEAN$s||CHR(40)  ||'8020'  ||CHR(41)  ||CODIGO  ||SUBSTR(UP.FACTURA_ACUERDO,1,9)  ||CHR(40)  ||'3900'  ||CHR(41)  ||LPAD(RP.PREVAL,14,0)  ||CHR(40)  ||'96'  ||CHR(41)  ||TO_CHAR(RP.PREFECLIM,'YYYYMMDD'),3) AS PIEBARRA,
  TO_CHAR(PCK_CODIGODEBARRAS.FC_IMPRIMIRCODIGODEBARRAS(CHR(205)  ||CHR(102)  ||'415'  ||s$strCodigoEAN$s||'8020'  ||CODIGO  ||SUBSTR(UP.FACTURA_ACUERDO,1,9)  ||CHR(102)  ||'3900'  ||LPAD(RP.PREVAL,14,0)  ||CHR(102)  ||'96'  ||TO_CHAR(RP.PREFECLIM,'YYYYMMDD'))) AS CODIGODEBARRAS_BIN
FROM IP_USUARIOS_PREDIAL UP
INNER JOIN IP_RECIBOS_DE_PAGO RP
ON UP.COMPANIA         = RP.COMPANIA
AND UP.CODIGO          = RP.PRECOD
AND UP.NUMERO_ORDEN    = RP.NUMERO_ORDEN
AND UP.FACTURA_ACUERDO = RP.DOCNUM 
INNER JOIN IP_DETALLE_RECIBOPAGO DR
ON RP.COMPANIA           = DR.COMPANIA
AND RP.DOCNUM            = DR.DOCNUM
WHERE UP.COMPANIA = s$compania$s
AND UP.CODIGO = s$codigo$s
AND UP.NUMERO_ORDEN = s$numeroOrden$s
