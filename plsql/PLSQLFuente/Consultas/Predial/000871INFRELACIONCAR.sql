SELECT IP_RECIBOS_DE_PAGO.PREFEC, 
       IP_RECIBOS_DE_PAGO.DOCNUM, 
       IP_RECIBOS_DE_PAGO.PRECOD, 
        CASE WHEN TO_CHAR(PREANOI) IS NULL   
          THEN TO_CHAR(VIGENCIA) 
          ELSE  
            CASE WHEN TO_CHAR(PREANOI) = TO_CHAR(PREANOF) 
              THEN TO_CHAR(VIGENCIA) 
              ELSE TO_CHAR(PREANOI) 
            END  
         END || '-' || TO_CHAR(PREANOF) AS ANIOS, 
        PREANO AS VIGENCIA, 
        IP_USUARIOS_PREDIAL.NUMERO_ORDEN, 
        IP_USUARIOS_PREDIAL.TRPCOD || ' - ' || IP_TARIFAS.TRPPOR AS TARIFA, 
        IP_USUARIOS_PREDIAL.AVALUO_ANO, 
        IP_RECIBOS_DE_PAGO.PREFECPAG, 
        IP_RECIBOS_DE_PAGO.C1 + IP_RECIBOS_DE_PAGO.C5 + IP_RECIBOS_DE_PAGO.C9 AS IMPUESTOPREDIAL, 
        IP_RECIBOS_DE_PAGO.C3 + IP_RECIBOS_DE_PAGO.C7 + IP_RECIBOS_DE_PAGO.C11 AS CAR, 
        IP_RECIBOS_DE_PAGO.C4 + IP_RECIBOS_DE_PAGO.C8 +  IP_RECIBOS_DE_PAGO.C12 AS INTERESCAR, 
        IP_RECIBOS_DE_PAGO.C3 + IP_RECIBOS_DE_PAGO.C4 + IP_RECIBOS_DE_PAGO.C7 + IP_RECIBOS_DE_PAGO.C8 + IP_RECIBOS_DE_PAGO.C11 + IP_RECIBOS_DE_PAGO.C12 AS TOTALCAR 
FROM IP_RECIBOS_DE_PAGO  
  INNER JOIN IP_USUARIOS_PREDIAL   
    ON  IP_RECIBOS_DE_PAGO.COMPANIA           = IP_USUARIOS_PREDIAL.COMPANIA 
    AND IP_RECIBOS_DE_PAGO.PRECOD             = IP_USUARIOS_PREDIAL.CODIGO 
    AND IP_RECIBOS_DE_PAGO.NUMERO_ORDEN       = IP_USUARIOS_PREDIAL.NUMERO_ORDEN 
  INNER JOIN IP_TARIFAS 
    ON IP_USUARIOS_PREDIAL.COMPANIA           = IP_TARIFAS.COMPANIA 
    AND IP_USUARIOS_PREDIAL.TRPCOD            = IP_TARIFAS.TRPCOD 
    AND IP_USUARIOS_PREDIAL.TRPANO            = IP_TARIFAS.TRPANO 
    AND IP_USUARIOS_PREDIAL.TRPRAN            = IP_TARIFAS.TRPRAN 
WHERE IP_RECIBOS_DE_PAGO.COMPANIA = s$compania$s 
  AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN =s$numeroOrden$s 
  AND IP_RECIBOS_DE_PAGO.PREFECPAG BETWEEN s$fechaInicial$s AND s$fechaFinal$s 
  AND IP_RECIBOS_DE_PAGO.PAGO NOT IN (0) 
ORDER BY IP_RECIBOS_DE_PAGO.DOCNUM
