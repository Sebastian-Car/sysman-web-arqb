SELECT 
  IP_ASOBANCARIA_LOG.FECHARECAUDO,
  IP_ASOBANCARIA_LOG.FECHA, 
  IP_ASOBANCARIA_LOG.HORA HORACARGA, 
  IP_ASOBANCARIA_LOG.USUARIO,
  IP_BANCOS.NOMBREBANCO BANCO,
  IP_ASOBANCARIA_LOG.UBICACION_ARCHIVO, 
  IP_ASOBANCARIA_PAGOS.CODIGO,
  IP_ASOBANCARIA_PAGOS.FACTURA, 
  IP_ASOBANCARIA_PAGOS.PREVAL, 
  IP_ASOBANCARIA_PAGOS.ID,
  SUBSTR(IP_ASOBANCARIA_ENC_ARC.HORA,0,2) || ':' || SUBSTR(IP_ASOBANCARIA_ENC_ARC.HORA,3,2) HORA 
FROM IP_ASOBANCARIA_ENC_ARC, IP_ASOBANCARIA_PAGOS 
  INNER JOIN IP_ASOBANCARIA_LOG 
     ON IP_ASOBANCARIA_PAGOS.COMPANIA = IP_ASOBANCARIA_LOG.COMPANIA
    AND IP_ASOBANCARIA_PAGOS.ID = IP_ASOBANCARIA_LOG.ID
  INNER JOIN IP_BANCOS
     ON IP_ASOBANCARIA_LOG.COMPANIA = IP_BANCOS.COMPANIA 
    AND IP_ASOBANCARIA_LOG.BANCO = IP_BANCOS.CODIGOBANCO 
WHERE IP_ASOBANCARIA_PAGOS.COMPANIA = s$compania$s
  AND s$fechaCampo$s = s$fecha$s
  AND IP_ASOBANCARIA_LOG.BANCO = 's$banco$s'
GROUP BY
  IP_ASOBANCARIA_LOG.FECHARECAUDO, IP_ASOBANCARIA_LOG.FECHA,  IP_ASOBANCARIA_LOG.HORA, IP_ASOBANCARIA_LOG.USUARIO, IP_BANCOS.NOMBREBANCO, IP_ASOBANCARIA_LOG.UBICACION_ARCHIVO, 
  IP_ASOBANCARIA_PAGOS.CODIGO, IP_ASOBANCARIA_PAGOS.FACTURA, IP_ASOBANCARIA_PAGOS.PREVAL, IP_ASOBANCARIA_PAGOS.ID, IP_ASOBANCARIA_ENC_ARC.HORA
