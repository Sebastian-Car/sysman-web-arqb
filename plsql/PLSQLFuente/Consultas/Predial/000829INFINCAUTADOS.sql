SELECT  IP_USUARIOS_PREDIAL.COMPANIA , 
        IP_USUARIOS_PREDIAL.CODIGO,
        IP_USUARIOS_PREDIAL.M_INMOBILIARIA,
        IP_USUARIOS_PREDIAL.NOMBRE,
        IP_USUARIOS_PREDIAL.NIT,
        IP_USUARIOS_PREDIAL.FECINICIAL_EXEINT,
        IP_USUARIOS_PREDIAL.FECFINAL_EXEINT,
        IP_USUARIOS_PREDIAL.OBSERVACIO         AS OBSERVACIONES,
        IP_USUARIOS_PREDIAL.RESOLUCION_INCAUTA AS NORESOLUCION
FROM IP_USUARIOS_PREDIAL
WHERE IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s
  AND IP_USUARIOS_PREDIAL.CODIGO BETWEEN s$codigoInicial$s AND s$codigoFinal$s
  AND TRUNC(IP_USUARIOS_PREDIAL.FECINICIAL_EXEINT) BETWEEN TO_DATE(s$fechaInicial$s, 'DD/MM/YYYY') AND TO_DATE(s$fechaFinal$s, 'DD/MM/YYYY')
  AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN = s$numeroOrden$s
  AND IP_USUARIOS_PREDIAL.IND_EXEINT NOT IN (0)
     
  
  AND IP_USUARIOS_PREDIAL.INDBORRADO IN (0) 
    
  
  AND IP_USUARIOS_PREDIAL.CODIGO_NO_ACTIVO IN (0)    
