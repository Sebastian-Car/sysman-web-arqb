SELECT 
  IP_ASOBANCARIA_LOG.FECHARECAUDO,
  IP_ASOBANCARIA_LOG.FECHA,
  IP_ASOBANCARIA_LOG.HORA HORACARGA,
  IP_BANCOS.NOMBREBANCO BANCO,
  IP_ASOBANCARIA_LOG.USUARIO,
  IP_ASOBANCARIA_LOG.ID, 
  SUBSTR(IP_ASOBANCARIA_ENC_ARC.HORA,0,2) || ':' || SUBSTR(IP_ASOBANCARIA_ENC_ARC.HORA,3,2) HORA, 
  IP_ASOBANCARIA_LOG.NUMERO_PAGOS, 
  IP_ASOBANCARIA_LOG.NUMERO_ERRORES,
  IP_ASOBANCARIA_LOG.UBICACION_ARCHIVO
FROM IP_ASOBANCARIA_ENC_ARC, IP_ASOBANCARIA_LOG
 INNER JOIN IP_BANCOS
     ON IP_ASOBANCARIA_LOG.COMPANIA = IP_BANCOS.COMPANIA 
    AND IP_ASOBANCARIA_LOG.BANCO = IP_BANCOS.CODIGOBANCO 
WHERE IP_ASOBANCARIA_LOG.COMPANIA = s$compania$s
  AND s$fechaCampo$s = s$fecha$s 
  AND IP_ASOBANCARIA_LOG.BANCO = 's$banco$s'
GROUP BY 
  IP_ASOBANCARIA_LOG.FECHARECAUDO, IP_ASOBANCARIA_LOG.FECHA, IP_ASOBANCARIA_LOG.HORA ,IP_BANCOS.NOMBREBANCO, IP_ASOBANCARIA_LOG.USUARIO, IP_ASOBANCARIA_LOG.ID,  
  IP_ASOBANCARIA_ENC_ARC.HORA, IP_ASOBANCARIA_LOG.NUMERO_PAGOS, IP_ASOBANCARIA_LOG.NUMERO_ERRORES, IP_ASOBANCARIA_LOG.UBICACION_ARCHIVO
