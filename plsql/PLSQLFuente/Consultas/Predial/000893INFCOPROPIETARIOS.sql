SELECT 
    IP_USUARIOS_PREDIAL.NIT, 
    IP_USUARIOS_PREDIAL.NOMBRE 
FROM 
    IP_USUARIOS_PREDIAL 
WHERE 
    IP_USUARIOS_PREDIAL.COMPANIA =  s$compania$s  
        AND 
    IP_USUARIOS_PREDIAL.CODIGO = 's$codigoPredio$s' 
        AND 
    IP_USUARIOS_PREDIAL.NUMERO_ORDEN NOT IN('s$numeroOrden$s')
