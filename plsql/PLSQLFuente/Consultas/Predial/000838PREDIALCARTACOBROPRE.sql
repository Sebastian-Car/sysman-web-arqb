SELECT IP_USUARIOS_PREDIAL.NOMBRE,  
       IP_USUARIOS_PREDIAL.CODIGO,  
       IP_USUARIOS_PREDIAL.TOTAL,  
       IP_USUARIOS_PREDIAL.DIRECCION_CORRESPONDENCIA,  
       IP_USUARIOS_PREDIAL.NIT,  
       IP_USUARIOS_PREDIAL.PAGO_ANO,  
       IP_USUARIOS_PREDIAL.NUMERO_ORDEN,  
       IP_USUARIOS_PREDIAL.INDBORRADO,  
       IP_USUARIOS_PREDIAL.CODIGO_NO_ACTIVO,  
       TO_CHAR(SYSDATE, 'YYYY') - IP_USUARIOS_PREDIAL.PAGO_ANO  EXPR1,  
       IP_USUARIOS_PREDIAL.DIRECCION  
FROM   IP_USUARIOS_PREDIAL  
WHERE  IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s 
AND    IP_USUARIOS_PREDIAL.CODIGO BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'  
AND    IP_USUARIOS_PREDIAL.NUMERO_ORDEN = 's$numeroOrden$s'  
AND    IP_USUARIOS_PREDIAL.INDBORRADO IN (0) 
AND    IP_USUARIOS_PREDIAL.CODIGO_NO_ACTIVO IN (0) 
AND    TO_CHAR(SYSDATE, 'YYYY') - IP_USUARIOS_PREDIAL.PAGO_ANO > s$anosDeuda$s  
AND    IP_USUARIOS_PREDIAL.TOTAL > s$deudas$s
