SELECT  s$tipoPredio$s AS TIPO_PREDIO,  
          COUNT(IP_USUARIOS_PREDIAL.CODIGO) AS NUMERO_PREDIOS,  
          COUNT(IP_USUARIOS_PREDIAL.NIT) AS NUMERO_PROPIE, 
          SUM(IP_USUARIOS_PREDIAL.AREA_HA) AS SUPERFICIE_HA,  
          SUM(IP_USUARIOS_PREDIAL.AREA_CONSTRUIDA) AS TOTAL_AREACONST,  
          SUM(IP_USUARIOS_PREDIAL.AVALUO_ANO) AS TOTAL_AVALUO  
  FROM IP_USUARIOS_PREDIAL  
  WHERE IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s
    AND IP_USUARIOS_PREDIAL.CODIGO BETWEEN s$codigoInicial$s AND s$codigoFinal$s  
    s$condBloqueado$s  
    AND IP_USUARIOS_PREDIAL.CODIGO_NO_ACTIVO IN(0)
    AND IP_USUARIOS_PREDIAL.INDBORRADO IN(0) 
   AND  CODIGO_NO_ACTIVO IN (0)   
    s$condicion$s
    s$union$s
