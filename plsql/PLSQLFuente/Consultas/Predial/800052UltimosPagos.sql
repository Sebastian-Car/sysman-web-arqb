SELECT  IP_USUARIOS_PREDIAL.CODIGO,  
        IP_USUARIOS_PREDIAL.NOMBRE AS NOMBRE_PROPIETARIO,  
        IP_RECIBOS_DE_PAGO.PREVAL AS ULTIMO_PAGO,  
        TO_CHAR(IP_USUARIOS_PREDIAL.PAG_FEC, 'DD/MM/YYYY') AS FECHA_PAGO,  
        IP_RECIBOS_DE_PAGO.PREANOI || '-' || IP_RECIBOS_DE_PAGO.PREANOF AS ANO_PAGO,  
        IP_RECIBOS_DE_PAGO.C1   "s$nombreConcepto1$s",  
        IP_RECIBOS_DE_PAGO.C2   "s$nombreConcepto2$s",   
        IP_RECIBOS_DE_PAGO.C3   "s$nombreConcepto3$s",  
        IP_RECIBOS_DE_PAGO.C4   "s$nombreConcepto4$s",  
        IP_RECIBOS_DE_PAGO.C5 + IP_RECIBOS_DE_PAGO.C9 "s$nombreConcepto5$s",  
        IP_RECIBOS_DE_PAGO.C6 + IP_RECIBOS_DE_PAGO.C10 "s$nombreConcepto6$s",  
        IP_RECIBOS_DE_PAGO.C7 + IP_RECIBOS_DE_PAGO.C11 "s$nombreConcepto7$s",  
        IP_RECIBOS_DE_PAGO.C8 + IP_RECIBOS_DE_PAGO.C12  "s$nombreConcepto8$s" 
        s$camposAdicionales$s 
FROM IP_USUARIOS_PREDIAL  
  INNER JOIN IP_RECIBOS_DE_PAGO  
    ON IP_USUARIOS_PREDIAL.COMPANIA = IP_RECIBOS_DE_PAGO.COMPANIA 
    AND IP_USUARIOS_PREDIAL.CODIGO = IP_RECIBOS_DE_PAGO.PRECOD 
    AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN = IP_RECIBOS_DE_PAGO.NUMERO_ORDEN 
    AND IP_USUARIOS_PREDIAL.NUM_COM = IP_RECIBOS_DE_PAGO.DOCNUM 
WHERE IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s 
  s$condicion$s 
  AND IP_RECIBOS_DE_PAGO.PAGO <> 0   
 ORDER BY  s$ordenamiento$s
