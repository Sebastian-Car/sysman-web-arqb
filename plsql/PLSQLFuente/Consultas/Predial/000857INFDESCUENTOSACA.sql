SELECT  IP_FACTURADOS.DOCNUM AS RECIBONUM,
        IP_FACTURADOS.CODIGO,
        IP_USUARIOS_PREDIAL.NOMBRE,
        IP_FACTURADOS.PREANO,
        IP_FACTURADOS.C1  AS IMPUESTO,
        IP_FACTURADOS.C2  AS INTERESES,
        IP_FACTURADOS.C13 AS DESCUENTOS,
        IP_FACTURADOS.C3  AS CORMACARENA,
        IP_FACTURADOS.C4  AS INTCORMACARENA,
        IP_FACTURADOS.C14 AS IAYSOB,
        IP_FACTURADOS.TOTAL,
        IP_FACTURADOS.DESESPC2,
        IP_FACTURADOS.DESESPC4,
        IP_FACTURADOS.C17,
        IP_FACTURADOS.FECHAPAGO,
        IP_FACTURADOS.CODIGO
FROM IP_FACTURADOS
  INNER JOIN IP_USUARIOS_PREDIAL
    	ON IP_FACTURADOS.COMPANIA 	= IP_USUARIOS_PREDIAL.COMPANIA
 	AND IP_FACTURADOS.CODIGO      	= IP_USUARIOS_PREDIAL.CODIGO
    	AND IP_FACTURADOS.NUMERO_ORDEN 	= IP_USUARIOS_PREDIAL.NUMERO_ORDEN
   
WHERE IP_FACTURADOS.COMPANIA = s$compania$s
  AND IP_FACTURADOS.FECHAPAGO BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND IP_FACTURADOS.NUMERO_ORDEN = s$numeroOrden$s
  AND IP_FACTURADOS.CODIGO BETWEEN s$codigoInicial$s AND s$codigoFinal$s
  AND IP_FACTURADOS.DESESPC2  + IP_FACTURADOS.DESESPC4  +
      IP_FACTURADOS.DESESPC13 + IP_FACTURADOS.DESESPC14 +
      IP_FACTURADOS.DESESPC15 + IP_FACTURADOS.DESESPC16 +
      IP_FACTURADOS.DESESPC17 + IP_FACTURADOS.DESESPC18 +
      IP_FACTURADOS.DESESPC19 +IP_FACTURADOS.DESESPC20  >0
  AND IP_FACTURADOS.PAGADO NOT IN (0)
ORDER BY IP_FACTURADOS.CODIGO
