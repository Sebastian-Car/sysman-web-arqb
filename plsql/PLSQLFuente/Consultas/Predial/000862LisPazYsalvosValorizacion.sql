SELECT 
  COMPANIA,
  NUMCER,
  PREDIO CODIGOPREDIO,
  CASE WHEN ANULADO NOT IN (0) THEN 'X' ELSE ' ' END ANULADO,
  NOMBRE,
  CASE WHEN ANULADO NOT IN (0) THEN 0 ELSE VALOR END VLR
FROM IP_CERTIFICADOVALORIZACION
WHERE COMPANIA = s$compania$s
  AND TRUNC(FECHA_EXPEDICION) BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
  s$conAnulados$s
