SELECT IP_FACTURADOS.CODIGO,
  IP_FACTURADOS.PREANO,
  C1+C2+C3+C4+C13+C14+C15+C16+C17+C18+C19+C20 TOTALP,
  IP_FACTURADOS.C1,
  IP_FACTURADOS.C2,
  IP_FACTURADOS.C3,
  IP_FACTURADOS.C4,
  IP_FACTURADOS.C13,
  IP_FACTURADOS.C14,
  IP_FACTURADOS.C15,
  IP_FACTURADOS.C16,
  IP_FACTURADOS.C17,
  IP_FACTURADOS.C18,
  IP_FACTURADOS.C19,
  IP_FACTURADOS.C20
FROM IP_FACTURADOS
WHERE IP_FACTURADOS.COMPANIA   = s$compania$s
AND IP_FACTURADOS.NUMERO_ORDEN = 's$nOrden$s'
AND IP_FACTURADOS.CODIGO BETWEEN 's$predioInicial$s' AND 's$predioFinal$s'
AND IP_FACTURADOS.FECHAPRESCRIPCION BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
AND IP_FACTURADOS.NOCOBRADO NOT IN (0)
AND IP_FACTURADOS.INDEXE        IN (0)
AND IP_FACTURADOS.INDCAR        IN (0)
ORDER BY IP_FACTURADOS.CODIGO,
  IP_FACTURADOS.PREANO
