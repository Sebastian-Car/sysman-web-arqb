WITH RS AS (
SELECT 
   CASE WHEN UP.AREA_HA+ UP.AREA_M2/10000  BETWEEN 0 AND 1.0
	    THEN 01
	    WHEN UP.AREA_HA+ UP.AREA_M2/10000 BETWEEN 1.0 AND 3.0
	    THEN 02
	    WHEN UP.AREA_HA+UP.AREA_M2/10000 BETWEEN 3.0 AND 5.0
	    THEN 03
	    WHEN UP.AREA_HA+ UP.AREA_M2/10000 BETWEEN 5.0 AND 10.0
	    THEN 04
	    WHEN UP.AREA_HA+ UP.AREA_M2/10000 BETWEEN 10.0 AND 15.0
	    THEN 05
	    WHEN UP.AREA_HA+ UP.AREA_M2/10000 BETWEEN 15.0 AND 20.0
	    THEN 06
	    WHEN UP.AREA_HA+ UP.AREA_M2/10000 BETWEEN 20.0 AND 50.0
	    THEN 07
	    WHEN UP.AREA_HA+ UP.AREA_M2/10000 BETWEEN 50.0 AND 100.0
	    THEN 08
	    WHEN UP.AREA_HA+ UP.AREA_M2/10000 BETWEEN 100.0 AND 200.0
	    THEN 09
	    WHEN UP.AREA_HA+ UP.AREA_M2/10000 BETWEEN 200.0 AND 500.0
	    THEN 10
	    WHEN UP.AREA_HA+ UP.AREA_M2/10000 BETWEEN 500.0 AND 1000.0
	    THEN 11
	    WHEN UP.AREA_HA+ UP.AREA_M2/10000 BETWEEN 1000.0 AND 2000.0
	    THEN 12
	    WHEN UP.AREA_HA+ UP.AREA_M2/10000 BETWEEN 2000.0 AND 99999999.0
	    THEN 13
   END RANGO
  ,UP.CODIGO
  ,UP.AREA_HA+ UP.AREA_M2/10000  AREAHEC
  ,UP.AREA_CONSTRUIDA AREACONS
  ,UP.AVALUO_ANO AVALUO
  ,UP.NUMERO_ORDEN
  ,UP.COMPANIA
FROM IP_USUARIOS_PREDIAL UP
WHERE UP.COMPANIA        = s$compania$s
  AND SUBSTR(UP.CODIGO,1,2) IN ('00')
  AND UP.AREA_CONSTRUIDA = 0 
), CANT_RANGO AS(
SELECT RS.RANGO, 
  COUNT(RS.RANGO) CANTRANGO
FROM RS
GROUP BY 
  RS.RANGO
), RS1 AS (
SELECT 
   CASE WHEN UP.AREA_HA*10000 + UP.AREA_M2  BETWEEN 0 AND 50
	    THEN 01
	    WHEN UP.AREA_HA*10000 + UP.AREA_M2  BETWEEN 50 AND 100
	    THEN 02
	    WHEN UP.AREA_HA*10000 + UP.AREA_M2  BETWEEN 100 AND 200
	    THEN 03
	    WHEN UP.AREA_HA*10000 + UP.AREA_M2  BETWEEN 200 AND 300
	    THEN 04
	    WHEN UP.AREA_HA*10000 + UP.AREA_M2  BETWEEN 300 AND 400
	    THEN 05
	    WHEN UP.AREA_HA*10000 + UP.AREA_M2  BETWEEN 400 AND 500
	    THEN 06
	    WHEN UP.AREA_HA*10000 + UP.AREA_M2  BETWEEN 500 AND 1000
	    THEN 07
	    WHEN UP.AREA_HA*10000 + UP.AREA_M2  BETWEEN 1000 AND 2000
	    THEN 08
	    WHEN UP.AREA_HA*10000 + UP.AREA_M2  BETWEEN 2000 AND 3000
	    THEN 09
	    WHEN UP.AREA_HA*10000 + UP.AREA_M2  BETWEEN 3000 AND 4000
	    THEN 10
	    WHEN UP.AREA_HA*10000 + UP.AREA_M2  BETWEEN 4000 AND 5000
	    THEN 11
	    WHEN UP.AREA_HA*10000 + UP.AREA_M2  BETWEEN 5000 AND 10000
	    THEN 12
	    WHEN UP.AREA_HA*10000 + UP.AREA_M2  BETWEEN 10000 AND 99999999
	    THEN 13
   END RANGO
  ,UP.CODIGO
  ,UP.AREA_HA+ UP.AREA_M2/10000  AREAHEC
  ,UP.AREA_CONSTRUIDA AREACONS
  ,UP.AVALUO_ANO AVALUO
  ,UP.NUMERO_ORDEN
  ,UP.COMPANIA
FROM IP_USUARIOS_PREDIAL UP
WHERE UP.COMPANIA = s$compania$s
  AND SUBSTR(UP.CODIGO,1,2) IN ('01')
  AND UP.AREA_CONSTRUIDA = 0 
), CANT_RANGO1 AS(
SELECT RS1.RANGO, 
  COUNT(RS1.RANGO) CANTRANGO
FROM RS1
GROUP BY 
  RS1.RANGO
)
SELECT 
   COUNT(RS.CODIGO) PREDIOS
  ,SUM(RS.AREAHEC) AREAHEC
  ,RS.RANGO CODRANGO
  ,DECODE(RS.RANGO
  	  ,1,'0 - 1 Has'
	  ,2,'1 - 3 Has'
	  ,3,'3 - 5 Has'
	  ,4,'5 - 10 Has'
	  ,5,'10 - 15 Has'
	  ,6,'15 - 20 Has'
	  ,7,'20 - 50 Has'
	  ,8,'50 - 100 Has'
	  ,9,'100 - 200 Has'
	  ,10,'200 - 500 Has'
	  ,11,'500 - 1000 Has'
	  ,12,'1000 - 2000 Has'
	  ,13,'2000 - 99999999 Has') RANGO
  ,CANT_RANGO.CANTRANGO
  ,SUM(RS.AREACONS) AREACONS
  ,SUM(RS.AVALUO) AVALUO
  ,1 UNIDAD
  ,C.NOMBRE COMPANIA
FROM RS
  INNER JOIN CANT_RANGO
    ON CANT_RANGO.RANGO = RS.RANGO
  INNER JOIN COMPANIA C
    ON C.CODIGO = RS.COMPANIA
WHERE RS.NUMERO_ORDEN= '001'
GROUP BY 
   C.NOMBRE
  ,RS.RANGO
  ,RS.RANGO 
  ,CANT_RANGO.CANTRANGO
UNION ALL
SELECT 
   COUNT(RS1.CODIGO) PREDIOS
  ,SUM(RS1.AREAHEC) AREAHEC
  ,RS1.RANGO
  ,DECODE(RS1.RANGO
  	  ,1,'0 - 50 M2'
	  ,2,'50 - 100 M2'
	  ,3,'100 - 200 M2'
	  ,4,'200 - 300 M2'
	  ,5,'300 - 400 M2'
	  ,6,'400 - 500 M2'
	  ,7,'500 - 1000 M2'
	  ,8,'1000 - 2000 M2'
	  ,9,'2000 - 3000 M2'
	  ,10,'3000 - 4000 M2'
	  ,11,'4000 - 5000 M2'
	  ,12,'5000 - 10000 M2'
	  ,13,'10000 - 99999999 M2') RANGO
  ,CANT_RANGO1.CANTRANGO
  ,SUM(RS1.AREACONS) AREACONS
  ,SUM(RS1.AVALUO) AVALUO
  ,2
  ,C.NOMBRE
FROM RS1
  INNER JOIN CANT_RANGO1
    ON CANT_RANGO1.RANGO = RS1.RANGO
  INNER JOIN COMPANIA C
    ON C.CODIGO = RS1.COMPANIA
WHERE RS1.NUMERO_ORDEN= '001'
GROUP BY
   C.NOMBRE 
  ,RS1.RANGO
  ,RS1.RANGO 
  ,CANT_RANGO1.CANTRANGO
ORDER BY 
  UNIDAD
  ,CODRANGO
