SELECT DISTINCT 
       IP_BANCOS.NOMBREBANCO,
       IP_RECIBOS_DE_PAGO.PREFECPAG,
       TO_CHAR(IP_RECIBOS_DE_PAGO.PREFECPAG, 'DD/MM/YYYY') FECHAPAG,
       IP_RECIBOS_DE_PAGO.DOCNUM,
       IP_RECIBOS_DE_PAGO.PRECOD,
       IP_USUARIOS_PREDIAL.NOMBRE,
       IP_RECIBOS_DE_PAGO.PAG_BAN,
       IP_RECIBOS_DE_PAGO.C1 AS C1,
       IP_RECIBOS_DE_PAGO.C13 * s$porcDescPredial$s SUMAPORC,
       IP_RECIBOS_DE_PAGO.C5 AS C5,
       IP_RECIBOS_DE_PAGO.C9 AS C9,
       IP_RECIBOS_DE_PAGO.C2 + IP_RECIBOS_DE_PAGO.C6 + IP_RECIBOS_DE_PAGO.C10 SUMA1,
       IP_RECIBOS_DE_PAGO.C18 AS C18,
       IP_RECIBOS_DE_PAGO.C19 AS C19,
       IP_RECIBOS_DE_PAGO.C14 + IP_RECIBOS_DE_PAGO.C15 + IP_RECIBOS_DE_PAGO.C16 + IP_RECIBOS_DE_PAGO.C17 + IP_RECIBOS_DE_PAGO.C20 SUMA2,
       NVL(IP_RECIBOS_DE_PAGO.INTERES_ACUERDO,0) AS INT_ACUERDO,
       NVL(IP_RECIBOS_DE_PAGO.INTERES_RECARGO,0) AS INT_RECARGO, 
       NVL(IP_RECIBOS_DE_PAGO.C1,0)+NVL(IP_RECIBOS_DE_PAGO.C2,0)+NVL(IP_RECIBOS_DE_PAGO.C5,0)+NVL(IP_RECIBOS_DE_PAGO.C6,0)+NVL(IP_RECIBOS_DE_PAGO.C9,0)+NVL(IP_RECIBOS_DE_PAGO.C10,0)+NVL(IP_RECIBOS_DE_PAGO.c13,0)+NVL(IP_RECIBOS_DE_PAGO.C14,0)+NVL(IP_RECIBOS_DE_PAGO.C15,0)+NVL(IP_RECIBOS_DE_PAGO.C16,0)+NVL(IP_RECIBOS_DE_PAGO.C17,0)+NVL(IP_RECIBOS_DE_PAGO.C18,0)+NVL(IP_RECIBOS_DE_PAGO.C19,0)+NVL(IP_RECIBOS_DE_PAGO.C20,0)+NVL(IP_RECIBOS_DE_PAGO.INTERES_ACUERDO,0)+NVL(IP_RECIBOS_DE_PAGO.INTERES_RECARGO,0)
 AS VALORFACTURADO
FROM   IP_RECIBOS_DE_PAGO
INNER JOIN IP_USUARIOS_PREDIAL
 ON  IP_RECIBOS_DE_PAGO.COMPANIA     = IP_USUARIOS_PREDIAL.COMPANIA
 AND IP_RECIBOS_DE_PAGO.PRECOD       = IP_USUARIOS_PREDIAL.CODIGO
 AND IP_RECIBOS_DE_PAGO.NUMERO_ORDEN = IP_USUARIOS_PREDIAL.NUMERO_ORDEN
INNER JOIN IP_BANCOS
 ON  IP_RECIBOS_DE_PAGO.COMPANIA     = IP_BANCOS.COMPANIA
 AND IP_RECIBOS_DE_PAGO.PAG_BANPAG   = IP_BANCOS.CODIGOBANCO
WHERE IP_RECIBOS_DE_PAGO.COMPANIA = s$compania$s
AND IP_RECIBOS_DE_PAGO.ANULADO IN (0)
AND IP_RECIBOS_DE_PAGO.PAGO NOT  IN (0)
AND IP_RECIBOS_DE_PAGO.PREFECPAG BETWEEN s$fechaInicial$s AND s$fechaFinal$s
AND IP_RECIBOS_DE_PAGO.PAG_BANPAG BETWEEN 's$codigoBancoInicial$s' AND 's$codigoBancoFin$s'
AND IP_RECIBOS_DE_PAGO.PAG_BANPAG  NOT IN ('s$prcodbanccompen$s')
AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN   = 's$numeroOrden$s'
AND IP_RECIBOS_DE_PAGO.PAQUETEPAG BETWEEN 's$cmbPaqIni$s' AND 's$cmbPaqFin$s'
ORDER BY IP_RECIBOS_DE_PAGO.DOCNUM
