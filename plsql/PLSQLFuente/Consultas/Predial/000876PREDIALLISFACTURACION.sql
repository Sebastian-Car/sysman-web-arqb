WITH CNS_CARTERA 
AS 
   ( SELECT FACTURADOS.CODIGO, 
           MAX(FACTURADOS.PREANO) AS ANO, 
           SUM(FACTURADOS.TOTAL) AS STOTAL, 
           SUM(FACTURADOS.C1) AS SC1, 
           SUM(FACTURADOS.C2) AS SC2, 
           SUM(FACTURADOS.C3) AS SC3, 
           SUM(FACTURADOS.C4) AS SC4, 
           SUM(FACTURADOS.C5) AS SC5, 
           SUM(FACTURADOS.C6) AS SC6, 
           SUM(FACTURADOS.C7) AS SC7, 
           SUM(FACTURADOS.C8) AS SC8, 
           SUM(FACTURADOS.C9) AS SC9, 
           SUM(FACTURADOS.C10) AS SC10, 
           SUM(FACTURADOS.C11) AS SC11,
           SUM(FACTURADOS.C12) AS SC12, 
           SUM(FACTURADOS.C13) AS SC13, 
           SUM(FACTURADOS.C14) AS SC14,
           SUM(FACTURADOS.C15) AS SC15,
           SUM(FACTURADOS.C16) AS SC16,
           SUM(FACTURADOS.C17) AS SC17, 
           SUM(FACTURADOS.C18) AS SC18, 
           SUM(FACTURADOS.C19) AS SC19, 
           SUM(FACTURADOS.C20) AS SC20
    FROM IP_FACTURADOS FACTURADOS
    WHERE FACTURADOS.COMPANIA      = s$compania$s
      AND FACTURADOS.NUMERO_ORDEN  = 's$numeroOrden$s' 
      AND FACTURADOS.INDCAR        IN(0) 
      AND FACTURADOS.INDEXE        IN(0)
      s$condicion$s
    GROUP BY FACTURADOS.CODIGO, FACTURADOS.NUMERO_ORDEN, FACTURADOS.INDCAR, FACTURADOS.INDEXE
    ORDER BY ANO, FACTURADOS.CODIGO ) 
SELECT U.CODIGO, 
       U.NOMBRE, 
       U.DIRECCION, 
       U.PAGO_ANO, 
       CSTD.SC1 AS C1, 
       CSTD.SC2 AS C2, 
       CSTD.SC3 AS C3, 
       CSTD.SC4 AS C4, 
       CSTD.SC13 AS C13,
       CSTD.SC14 AS C14,
       CSTD.SC15 AS C15,
       CSTD.SC16 AS C16, 
       CSTD.SC17 AS C17, 
       CSTD.SC18 AS C18, 
       CSTD.SC19 AS C19, 
       CSTD.SC20 AS C20, 
       SC1+SC2+SC3+SC4+SC13+SC14+SC15+SC16+SC17+SC18+SC19+SC20 AS TOTALFACTURADO, 
       U.M_INMOBILIARIA,
       CSTD.SC14 +CSTD.SC15 +CSTD.SC16 +CSTD.SC17 +CSTD.SC18 +CSTD.SC19 + CSTD.SC20  OTROS
FROM IP_USUARIOS_PREDIAL U INNER JOIN CNS_CARTERA CSTD 
      ON U.CODIGO = CSTD.CODIGO
WHERE U.COMPANIA     = s$compania$s
  AND U.NUMERO_ORDEN = 's$numeroOrden$s' 
  AND U.INDCAR       IN(0)
  AND U.INDEXE       IN(0)
  AND SC1+SC2+SC3+SC4+SC13+SC14+SC15+SC16+SC17+SC18+SC19+SC20        > s$masDeCuanto$s  
  AND U.CODIGO BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 
  AND U.INDBORRADO  IN (0)
  AND U.CODIGO_NO_ACTIVO IN (0) 
ORDER BY U.CODIGO
