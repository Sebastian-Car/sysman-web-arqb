SELECT  IP_RECIBOS_DE_PAGO.PREFEC FECHA, 
        'N' TIPO, 
        'EMITIDA' NOMBRETIPO, 
        IP_RECIBOS_DE_PAGO.DOCNUM, 
        IP_RECIBOS_DE_PAGO.PRECOD, 
        IP_RECIBOS_DE_PAGO.C1, 
        IP_RECIBOS_DE_PAGO.C13, 
        (IP_RECIBOS_DE_PAGO.C1 + IP_RECIBOS_DE_PAGO.C13) IMPNETO, 
        IP_RECIBOS_DE_PAGO.C5, 
        IP_RECIBOS_DE_PAGO.C9, 
        (IP_RECIBOS_DE_PAGO.C1 + IP_RECIBOS_DE_PAGO.C5 + IP_RECIBOS_DE_PAGO.C9)  AS IMPUESTO, 
        (IP_RECIBOS_DE_PAGO.C2 + IP_RECIBOS_DE_PAGO.C6 + IP_RECIBOS_DE_PAGO.C10) AS INTERES, 
         IP_RECIBOS_DE_PAGO.C14, 
         IP_RECIBOS_DE_PAGO.C15, 
         IP_RECIBOS_DE_PAGO.C17, 
         IP_RECIBOS_DE_PAGO.C18, 
         IP_RECIBOS_DE_PAGO.C19, 
         IP_RECIBOS_DE_PAGO.C16, 
         IP_RECIBOS_DE_PAGO.C20, 
         IP_RECIBOS_DE_PAGO.PREVAL, 
         DECODE(IP_RECIBOS_DE_PAGO.ANULADO , '0', ' ' , '-1' ,'X' )ANULADO, 
         IP_RECIBOS_DE_PAGO.FECHAANULACION         AS FECHAANUL 
FROM IP_USUARIOS_PREDIAL 
  INNER JOIN IP_RECIBOS_DE_PAGO 
    ON IP_USUARIOS_PREDIAL.COMPANIA = IP_RECIBOS_DE_PAGO.COMPANIA 
    AND IP_USUARIOS_PREDIAL.CODIGO = IP_RECIBOS_DE_PAGO.PRECOD 
    AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN = IP_RECIBOS_DE_PAGO.NUMERO_ORDEN 
WHERE IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s 
  AND IP_USUARIOS_PREDIAL.PAGO_ANO >= s$ano$s 
  AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN = s$numeroOrden$s 
  AND TRUNC(IP_RECIBOS_DE_PAGO.PREFEC) BETWEEN 's$fechaInicial$s' AND 's$fechaFinal$s' 
UNION  
SELECT   
        IP_RECIBOS_DE_PAGO.FECHAANULACION  FECHA, 
        'A'  TIPO, 
        'ANULADA' NOMBRETIPO, 
        IP_RECIBOS_DE_PAGO.DOCNUM, 
        IP_RECIBOS_DE_PAGO.PRECOD, 
        (IP_RECIBOS_DE_PAGO.C1 *-1) C1, 
        (IP_RECIBOS_DE_PAGO.C13*-1) C13, 
        ((IP_RECIBOS_DE_PAGO.C1*-1) - IP_RECIBOS_DE_PAGO.C13)IMPNETO, 
        (IP_RECIBOS_DE_PAGO.C5*-1) C5, 
        (IP_RECIBOS_DE_PAGO.C9*-1) C9, 
        ((IP_RECIBOS_DE_PAGO.C1 + IP_RECIBOS_DE_PAGO.C5 + IP_RECIBOS_DE_PAGO.C9)*-1)  AS IMPUESTO, 
        ((IP_RECIBOS_DE_PAGO.C2 + IP_RECIBOS_DE_PAGO.C6 + IP_RECIBOS_DE_PAGO.C10)*-1) AS INTERES, 
        (IP_RECIBOS_DE_PAGO.C14*-1)C14, 
        (IP_RECIBOS_DE_PAGO.C15*-1)C15, 
        (IP_RECIBOS_DE_PAGO.C17*-1)C17, 
        (IP_RECIBOS_DE_PAGO.C18*-1)C18, 
        (IP_RECIBOS_DE_PAGO.C19*-1)C19, 
        (IP_RECIBOS_DE_PAGO.C16*-1)C16, 
        (IP_RECIBOS_DE_PAGO.C20*-1)C20, 
        (IP_RECIBOS_DE_PAGO.PREVAL *-1)PREVAL,   
        DECODE(IP_RECIBOS_DE_PAGO.ANULADO , '0', ' ' , '-1' ,'X' )ANULADO, 
        IP_RECIBOS_DE_PAGO.FECHAANULACION FECHAANUL 
FROM IP_USUARIOS_PREDIAL 
  INNER JOIN IP_RECIBOS_DE_PAGO 
    ON IP_USUARIOS_PREDIAL.COMPANIA = IP_RECIBOS_DE_PAGO.COMPANIA 
    AND IP_USUARIOS_PREDIAL.CODIGO = IP_RECIBOS_DE_PAGO.PRECOD 
    AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN = IP_RECIBOS_DE_PAGO.NUMERO_ORDEN 
WHERE IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s 
  AND IP_USUARIOS_PREDIAL.PAGO_ANO >= s$ano$s 
  AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN = s$numeroOrden$s 
  AND IP_RECIBOS_DE_PAGO.ANULADO NOT IN(0) 
  AND TRUNC(IP_RECIBOS_DE_PAGO.FECHAANULACION) BETWEEN 's$fechaInicial$s' AND 's$fechaFinal$s' 
