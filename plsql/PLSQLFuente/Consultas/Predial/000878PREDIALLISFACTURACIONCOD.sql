SELECT * 
FROM (
WITH CNS_CARTERA_STD_SVAP_NA AS (
SELECT FACTURADOS.COMPANIA, 
       FACTURADOS.CODIGO, 
       FACTURADOS.PREANO, 
       SUM(FACTURADOS.TOTAL) STOTAL, 
       SUM(FACTURADOS.C1) SC1, 
       SUM(FACTURADOS.C2) SC2, 
       SUM(FACTURADOS.C3) SC3, 
       SUM(FACTURADOS.C4) SC4, 
       SUM(FACTURADOS.C5) SC5, 
       SUM(FACTURADOS.C6) SC6, 
       SUM(FACTURADOS.C7) SC7, 
       SUM(FACTURADOS.C8) SC8, 
       SUM(FACTURADOS.C9) SC9, 
       SUM(FACTURADOS.C10) SC10, 
       SUM(FACTURADOS.C11) SC11, 
       SUM(FACTURADOS.C12) SC12, 
       SUM(FACTURADOS.C13) SC13, 
       SUM(FACTURADOS.C14) SC14, 
       SUM(FACTURADOS.C15) SC15, 
       SUM(FACTURADOS.C16) SC16, 
       SUM(FACTURADOS.C17) SC17, 
       SUM(FACTURADOS.C18) SC18, 
       SUM(FACTURADOS.C19) SC19, 
       SUM(FACTURADOS.C20) SC20
FROM IP_FACTURADOS FACTURADOS
WHERE FACTURADOS.COMPANIA      = s$compania$s 
  AND FACTURADOS.NUMERO_ORDEN  = 's$numeroOrden$s'
  AND FACTURADOS.INDCAR        IN (0) 
  AND FACTURADOS.INDEXE        IN (0) 
  AND FACTURADOS.INDPAGO_ACPAG IN (0) 
GROUP BY FACTURADOS.CODIGO, 
         FACTURADOS.PREANO, 
         FACTURADOS.INDCAR, 
         FACTURADOS.INDEXE, 
         FACTURADOS.NUMERO_ORDEN,
         FACTURADOS.COMPANIA
)
SELECT CSTD.PREANO, 
       U.CODIGO, 
       U.NOMBRE, 
       U.DIRECCION, 
       CSTD.SC1 C1, 
       CSTD.SC2 C2, 
       CSTD.SC3 C3, 
       CSTD.SC4 C4, 
       CSTD.SC13 C13, 
       (CSTD.SC14+CSTD.SC15+CSTD.SC16+CSTD.SC17+CSTD.SC18+CSTD.SC19+CSTD.SC20) SUMA,
       CSTD.STOTAL TOTALANO
FROM IP_USUARIOS_PREDIAL U 
  INNER JOIN CNS_CARTERA_STD_SVAP_NA CSTD 
    ON  U.COMPANIA = CSTD.COMPANIA 
    AND U.CODIGO   = CSTD.CODIGO 
WHERE U.COMPANIA         = s$compania$s 
  AND U.NUMERO_ORDEN     = 's$numeroOrden$s'
  AND U.CODIGO BETWEEN 's$codigoInicial$s'
                   AND 's$codigoFinal$s'  
  AND CSTD.STOTAL        >= s$masDeCuanto$s
  AND U.CODIGO_NO_ACTIVO IN (0) 
  AND U.INDBORRADO       IN (0) 
  AND U.INDCAR           IN (0)
  AND U.INDEXE           IN (0)
ORDER BY U.CODIGO) PREDIAL_LISFACTURACIONCOD
WHERE s$conAcuerdo$s = 1
UNION ALL
SELECT * 
FROM (
WITH CNS_CARTERA_STD_TV_NA AS  (
SELECT FACTURADOS.COMPANIA, 
       FACTURADOS.CODIGO, 
       FACTURADOS.PREANO, 
       SUM(FACTURADOS.TOTAL) STOTAL, 
       SUM(FACTURADOS.C1) SC1, 
       SUM(FACTURADOS.C2) SC2, 
       SUM(FACTURADOS.C3) SC3, 
       SUM(FACTURADOS.C4) SC4, 
       SUM(FACTURADOS.C5) SC5, 
       SUM(FACTURADOS.C6) SC6, 
       SUM(FACTURADOS.C7) SC7, 
       SUM(FACTURADOS.C8) SC8, 
       SUM(FACTURADOS.C9) SC9, 
       SUM(FACTURADOS.C10) SC10, 
       SUM(FACTURADOS.C11) SC11, 
       SUM(FACTURADOS.C12) SC12, 
       SUM(FACTURADOS.C13) SC13, 
       SUM(FACTURADOS.C14) SC14, 
       SUM(FACTURADOS.C15) SC15, 
       SUM(FACTURADOS.C16) SC16, 
       SUM(FACTURADOS.C17) SC17, 
       SUM(FACTURADOS.C18) SC18, 
       SUM(FACTURADOS.C19) SC19, 
       SUM(FACTURADOS.C20) SC20
FROM IP_FACTURADOS FACTURADOS
WHERE FACTURADOS.COMPANIA     = s$compania$s 
  AND FACTURADOS.NUMERO_ORDEN = 's$numeroOrden$s'
 
  AND FACTURADOS.INDCAR        IN (0) 
  AND FACTURADOS.INDEXE        IN (0) 
GROUP BY FACTURADOS.COMPANIA, 
         FACTURADOS.CODIGO, 
         FACTURADOS.PREANO, 
         FACTURADOS.INDCAR, 
         FACTURADOS.INDEXE, 
         FACTURADOS.NUMERO_ORDEN
)
SELECT CSTD.PREANO,
       U.CODIGO, 
       U.NOMBRE, 
       U.DIRECCION, 
       CSTD.SC1 C1, 
       CSTD.SC2 C2, 
       CSTD.SC3 C3, 
       CSTD.SC4 C4, 
       CSTD.SC13 C13, 
       (SC14+SC15+SC16+SC17+SC18+SC19+SC20) OTROS, 
       (SC1+SC2+SC3+SC4+SC13+SC14+SC15+SC16+SC18+SC17+SC19+SC20) TOTALANO
FROM IP_USUARIOS_PREDIAL U 
  INNER JOIN CNS_CARTERA_STD_TV_NA CSTD 
    ON  U.COMPANIA = CSTD.COMPANIA
    AND U.CODIGO   = CSTD.CODIGO
WHERE U.COMPANIA     =  s$compania$s  
  AND U.NUMERO_ORDEN =  's$numeroOrden$s' 
  AND U.CODIGO BETWEEN 's$codigoInicial$s'
                   AND 's$codigoFinal$s' 
  AND CSTD.STOTAL    >= s$masDeCuanto$s
  AND U.CODIGO_NO_ACTIVO IN (0) 
  AND U.INDBORRADO       IN (0) 
  AND U.INDCAR           IN (0) 
  AND U.INDEXE           IN (0)
ORDER BY U.CODIGO
) PREDIAL_LISFACTURACIONCOD_CVA
WHERE s$conAcuerdo$s = 2
