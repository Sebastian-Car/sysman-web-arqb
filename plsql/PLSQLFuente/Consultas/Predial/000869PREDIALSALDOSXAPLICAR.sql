SELECT 
  PD.PRECOD,
  UP.NIT, 
  UP.NOMBRE, 
  PD.PREANO,
  PD.VALOR,
  PD.OBSERVACIONES
FROM IP_PAGOSDOBLES PD
  INNER JOIN IP_USUARIOS_PREDIAL UP
     ON PD.COMPANIA = UP.COMPANIA
    AND PD.PRECOD   = UP.CODIGO
WHERE PD.COMPANIA     = s$compania$s
  AND PD.PRECOD BETWEEN 's$predioInicial$s' AND 's$predioFinal$s'
  AND TRUNC(PD.FECHA)  BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
  AND PD.TIPO         = 's$tipo$s'
  AND UP.NUMERO_ORDEN = 's$nOrden$s'
  AND UP.INDBORRADO       IN (0)
  AND UP.CODIGO_NO_ACTIVO IN (0)
  AND PD.PAGO             IN (0)
