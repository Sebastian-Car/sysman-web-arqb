SELECT IP_NOTIFICACIONES.CONSECUTIVO,
IP_NOTIFICACIONES.CODPREDIO, 
IP_NOTIFICACIONES.FECHANOTIFICACION,
 IP_NOTIFICACIONES.DOCSOPORTE, 
 IP_NOTIFICACIONES.VALOR AS VRTOTAL, 
 IP_USUARIOS_PREDIAL.NOMBRE 
 FROM IP_NOTIFICACIONES 
 LEFT JOIN IP_USUARIOS_PREDIAL ON 
 IP_NOTIFICACIONES.COMPANIA = IP_USUARIOS_PREDIAL.COMPANIA
 AND IP_NOTIFICACIONES.CODPREDIO = IP_USUARIOS_PREDIAL.CODIGO
 AND IP_NOTIFICACIONES.NUMERO_ORDEN = IP_USUARIOS_PREDIAL.NUMERO_ORDEN
WHERE IP_NOTIFICACIONES.COMPANIA = s$compania$s
AND IP_NOTIFICACIONES.FECHANOTIFICACION
  BETWEEN s$fechaInicial$s AND s$fechaFinal$s 
ORDER BY CONSECUTIVO    
