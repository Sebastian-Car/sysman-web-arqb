SELECT DISTINCT 
    IP_RECIBOS_DE_PAGO.DOCNUM,
    IP_RECIBOS_DE_PAGO.PRECOD,
    IP_USUARIOS_PREDIAL.NOMBRE, 
    IP_RECIBOS_DE_PAGO.C1  C1, 
    IP_RECIBOS_DE_PAGO.C13  C13,
    IP_RECIBOS_DE_PAGO.C5 + IP_RECIBOS_DE_PAGO.C9 SUMA1S,
    IP_RECIBOS_DE_PAGO.C2  C2, 
    IP_RECIBOS_DE_PAGO.C6 +  IP_RECIBOS_DE_PAGO.C10 SUMA2S, 
    IP_RECIBOS_DE_PAGO.C1 + IP_RECIBOS_DE_PAGO.C2 +  IP_RECIBOS_DE_PAGO.C5 + IP_RECIBOS_DE_PAGO.C6 + IP_RECIBOS_DE_PAGO.C9 + IP_RECIBOS_DE_PAGO.C10 + IP_RECIBOS_DE_PAGO.C13 SUMA3S,
    IP_RECIBOS_DE_PAGO.C14 + IP_RECIBOS_DE_PAGO.C15 + IP_RECIBOS_DE_PAGO.C16 + IP_RECIBOS_DE_PAGO.C17 + IP_RECIBOS_DE_PAGO.C18 + IP_RECIBOS_DE_PAGO.C19 + IP_RECIBOS_DE_PAGO.C20 SUMA4S,
    IP_RECIBOS_DE_PAGO.C3  C3, 
    IP_RECIBOS_DE_PAGO.C7 + IP_RECIBOS_DE_PAGO.C11 SUMA5S, 
    IP_RECIBOS_DE_PAGO.C4  C4, 
    IP_RECIBOS_DE_PAGO.C8 + IP_RECIBOS_DE_PAGO.C12 SUMA6S,
    IP_RECIBOS_DE_PAGO.C3 + IP_RECIBOS_DE_PAGO.C4 + IP_RECIBOS_DE_PAGO.C7 + IP_RECIBOS_DE_PAGO.C8 + IP_RECIBOS_DE_PAGO.C11 + IP_RECIBOS_DE_PAGO.C12 SUMA7S,
    NVL(INTERES_ACUERDO, 0)  INT_ACUERDO, 
    NVL(INTERES_RECARGO, 0)  INT_RECARGO, 
    IP_RECIBOS_DE_PAGO.PREVAL
FROM IP_RECIBOS_DE_PAGO 
INNER JOIN IP_USUARIOS_PREDIAL  
ON  IP_RECIBOS_DE_PAGO.COMPANIA      = IP_USUARIOS_PREDIAL.COMPANIA
AND IP_RECIBOS_DE_PAGO.PRECOD        = IP_USUARIOS_PREDIAL.CODIGO
AND IP_RECIBOS_DE_PAGO.NUMERO_ORDEN  = IP_USUARIOS_PREDIAL.NUMERO_ORDEN
WHERE  IP_RECIBOS_DE_PAGO.COMPANIA = s$compania$s
AND    IP_RECIBOS_DE_PAGO.ANULADO = 0 
AND    IP_RECIBOS_DE_PAGO.PAGO NOT IN (0)
AND    IP_RECIBOS_DE_PAGO.PAG_BANPAG = $P{PR_CODIGOBANCOCOMPEN}
AND    IP_RECIBOS_DE_PAGO.PREFECPAG BETWEEN  s$fechaInicial$s AND  s$fechaFinal$s
AND    IP_USUARIOS_PREDIAL.NUMERO_ORDEN = 's$numeroOrden$s'
ORDER BY 
    IP_RECIBOS_DE_PAGO.DOCNUM
