SELECT COUNT(FACTURADOS.CODIGO) AS NO_PREDIOS, 
       FACTURADOS.PREANO AS ANO, 
       FACTURADOS.TRPCOD AS TARIFA, 
       TARIFAS.TRPDES AS DESCRIPCION, 
       TARIFAS.TRPRAN1 AS DESDE, 
       TARIFAS.TRPRAN2 AS HASTA, 
       FACTURADOS.TRPRAN AS RANGO, 
       ROUND(AVG(TARIFAS.TRPPOR/1000),4) AS PORCENTAJE, 
       SUM(USUARIOS_PREDIAL.AVALUO_ANO) AS TOTAL_AVALUOS, 
       ROUND((SUM(USUARIOS_PREDIAL.AVALUO_ANO))*(ROUND(AVG(TARIFAS.TRPPOR/1000),4)),0) AS IMPUESTO,
       COMPANIA.NOMBRE NOMBRECOMPANIA,
       EXTRACT(YEAR FROM SYSDATE) - 2 FECHAD,
       EXTRACT(YEAR FROM SYSDATE) FECHAH
  FROM IP_USUARIOS_PREDIAL USUARIOS_PREDIAL 
       INNER JOIN IP_FACTURADOS FACTURADOS  
          ON USUARIOS_PREDIAL.NUMERO_ORDEN = FACTURADOS.NUMERO_ORDEN 
         AND USUARIOS_PREDIAL.CODIGO = FACTURADOS.CODIGO
       INNER JOIN IP_TARIFAS TARIFAS 
          ON FACTURADOS.PREANO = TARIFAS.TRPANO
         AND FACTURADOS.TRPCOD = TARIFAS.TRPCOD
         AND FACTURADOS.TRPRAN = TARIFAS.TRPRAN
       INNER JOIN COMPANIA
          ON USUARIOS_PREDIAL.COMPANIA = COMPANIA.CODIGO
 WHERE USUARIOS_PREDIAL.COMPANIA = s$compania$s
   AND TARIFAS.TRPANO = FACTURADOS.PREANO
   AND USUARIOS_PREDIAL.NUMERO_ORDEN = '001' 
   AND USUARIOS_PREDIAL.INDBORRADO = 0 
   AND USUARIOS_PREDIAL.CODIGO_NO_ACTIVO = 0 
 
   AND FACTURADOS.PREANO > (EXTRACT (YEAR FROM SYSDATE)-3)
 GROUP BY FACTURADOS.PREANO, 
       FACTURADOS.TRPCOD, 
       TARIFAS.TRPDES, 
       TARIFAS.TRPRAN1, 
       TARIFAS.TRPRAN2, 
       FACTURADOS.TRPRAN, 
       TARIFAS.GRUPO_TARIFA,
       COMPANIA.NOMBRE,
       EXTRACT(YEAR FROM SYSDATE) - 2,
       EXTRACT(YEAR FROM SYSDATE) 
  

   

