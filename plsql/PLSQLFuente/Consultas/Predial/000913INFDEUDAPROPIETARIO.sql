SELECT F.CODIGO,
       F.NUMERO_ORDEN,
       U.NOMBRE,
       U.NIT,
       U.DIRECCION,
       U.AVALUO_ANO,
       U.TRPCOD,
       U.M_INMOBILIARIA,
       SUM(CASE WHEN F.PREANO = EXTRACT(YEAR FROM SYSDATE) 
       THEN F.C1 ELSE 0 END) IMPUESTO_ACTUAL,
       SUM(CASE WHEN F.PREANO = EXTRACT(YEAR FROM SYSDATE) 
       THEN F.C2 ELSE 0 END) INTERES_IMP_ACTUAL,
       SUM(CASE WHEN F.PREANO = EXTRACT(YEAR FROM SYSDATE) 
       THEN F.C3 ELSE 0 END) CAR_ACTUAL,
       SUM(CASE WHEN F.PREANO = EXTRACT(YEAR FROM SYSDATE) 
       THEN F.C4 ELSE 0 END) INTERES_CAR_ACTUAL,  
       SUM(CASE WHEN F.PREANO = (EXTRACT(YEAR FROM SYSDATE) -1)
       THEN F.C1 ELSE 0 END) IMPUESTO_ANTERIOR ,
       SUM(CASE WHEN F.PREANO = (EXTRACT(YEAR FROM SYSDATE) -1)
       THEN F.C2 ELSE 0 END) INTERES_IMP_ANTERIOR,
       SUM(CASE WHEN F.PREANO = (EXTRACT(YEAR FROM SYSDATE) -1)
       THEN F.C3 ELSE 0 END) CAR_ANTERIOR,
       SUM(CASE WHEN F.PREANO = (EXTRACT(YEAR FROM SYSDATE) -1)
       THEN F.C4 ELSE 0 END) INTERES_CAR_ANTERIOR,       
       SUM(CASE WHEN F.PREANO < (EXTRACT(YEAR FROM SYSDATE) -1)
       THEN F.C1 ELSE 0 END) IMPUESTO_DIF_RECAUDO,
       SUM(CASE WHEN F.PREANO < (EXTRACT(YEAR FROM SYSDATE) -1)
       THEN F.C2 ELSE 0 END) INTERES_DIF_RECAUDO,
       SUM(CASE WHEN F.PREANO < (EXTRACT(YEAR FROM SYSDATE) -1)
       THEN F.C3 ELSE 0 END) CAR_DIF_RECAUDO,
       SUM(CASE WHEN F.PREANO < (EXTRACT(YEAR FROM SYSDATE) -1)
       THEN F.C4 ELSE 0 END) INTERES_CAR_DIF_RECAUDO, 
       SUM(F.C13 + F.C14 + F.C15 + F.C16 + F.C17 + F.C18 + F.C19 + F.C20) OTROS
FROM IP_FACTURADOS F
 INNER JOIN IP_USUARIOS_PREDIAL U
    ON F.COMPANIA   = U.COMPANIA
   AND F.CODIGO = U.CODIGO
   AND F.NUMERO_ORDEN   = U.NUMERO_ORDEN
 WHERE F.COMPANIA = s$compania$s
   AND F.NUMERO_ORDEN = s$numeroOrden$s
   AND F.PAGADO = 0
   AND F.NOCOBRADO = 0
   AND F.INDCAR = 0
   AND F.INDEXE = 0
   AND U.INDBORRADO = 0
   AND U.CODIGO_NO_ACTIVO = 0
   AND F.TOTAL <> 0
   AND U.NIT BETWEEN s$personaInicial$s AND s$personaFinal$s
   AND F.INDPAGO_ACPAG = 0 
 GROUP BY F.COMPANIA,
          F.CODIGO,
          F.NUMERO_ORDEN,
          U.NOMBRE,
          U.NIT,
          U.DIRECCION,
          U.AVALUO_ANO,
          U.TRPCOD,
          U.M_INMOBILIARIA
