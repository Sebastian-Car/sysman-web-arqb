SELECT  IP_ACUERDOS.CODIGOACUERDO,
        IP_ACUERDOS.PREDIO,
        IP_ACUERDOS.FECHAACUERDO,
        IP_ACUERDOS.ID_RESP,
        IP_ACUERDOS.NOMBRE_RESP,
        IP_ACUERDOS.RESOLUCION,
        IP_ACUERDOS.DIRECCION_RESP,
        IP_ACUERDOS.TELEFONO_RESP,
        IP_FACTURADOSACUERDOS.CUOTA,
        IP_FACTURADOSACUERDOS.FECHAFACTURADO,
        IP_FACTURADOSACUERDOS.DOCNUM,
        IP_FACTURADOSACUERDOS.FECHAPAGO,
        IP_BANCOS.NOMBREBANCO PAG_BAN,
        IP_FACTURADOSACUERDOS.CAPITAL,
        IP_FACTURADOSACUERDOS.INTERESES,
        IP_FACTURADOSACUERDOS.INTERES_ACUERDO,
        IP_FACTURADOSACUERDOS.INTERES_RECARGO,
        NVL(IP_FACTURADOSACUERDOS.CAPITAL,0) +  NVL(IP_FACTURADOSACUERDOS.INTERESES,0) + NVL(IP_FACTURADOSACUERDOS.INTERES_ACUERDO,0) + NVL(IP_FACTURADOSACUERDOS.INTERES_RECARGO,0) TOTAL
FROM IP_ACUERDOS 
  INNER JOIN IP_FACTURADOSACUERDOS  
    ON 	IP_ACUERDOS.COMPANIA 		= IP_FACTURADOSACUERDOS.COMPANIA
    AND IP_ACUERDOS.CODIGOACUERDO 	= IP_FACTURADOSACUERDOS.CODIGOACUERDO
    AND IP_ACUERDOS.PREDIO 		= IP_FACTURADOSACUERDOS.PREDIO
    AND IP_ACUERDOS.NUMERO_ORDEN 	= IP_FACTURADOSACUERDOS.NUMERO_ORDEN
  LEFT JOIN IP_BANCOS
    ON  IP_FACTURADOSACUERDOS.COMPANIA  = IP_BANCOS.COMPANIA
    AND IP_FACTURADOSACUERDOS.PAG_BAN  	= IP_BANCOS.CODIGOBANCO 
WHERE IP_ACUERDOS.COMPANIA = s$compania$s
	s$condicion$s
