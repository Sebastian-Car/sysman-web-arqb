 WITH PREDIAL_LISIGACRESOLUCIONES AS (

SELECT   IGAC_RESOLUCIONES.RESOLUCION, 

         IGAC_RESOLUCIONES.FECHAINGRESOSISTEMA, 

         IGAC_RESOLUCIONESDET.CONSECUTIVO, 

         IGAC_RESOLUCIONESDET.MUTACION, 

         IGAC_RESOLUCIONESDET.CODIGO, 

         IGAC_RESOLUCIONESDET.CANCELAINSCRIBE, 

         IGAC_RESOLUCIONESDET.TIPOREGISTRO, 

         IGAC_RESOLUCIONESDET.NUMEROORDEN, 

         IGAC_RESOLUCIONESDET.TOTALREGISTROS, 

         IGAC_RESOLUCIONESDET.NOMBRE, 

         IGAC_RESOLUCIONESDET.ESTADOCIVIL, 

         IGAC_RESOLUCIONESDET.TIPODOCUMENTO, 

         IGAC_RESOLUCIONESDET.NUMERODOCUMENTO, 

         IGAC_RESOLUCIONESDET.DIRECCION, 

         IGAC_RESOLUCIONESDET.DESTINOECONOMICO, 

         IGAC_RESOLUCIONESDET.AREATERRENO, 

         IGAC_RESOLUCIONESDET.AREACONSTRUIDA, 

         IGAC_RESOLUCIONESDET.AVALUO, 

         IGAC_RESOLUCIONESDET.VIGENCIA, 

         IGAC_RESOLUCIONESDET.CONDICIONTRIS, 

         IGAC_RESOLUCIONESDET.ANTRESOLUCION, 

         IGAC_RESOLUCIONESDET.ANTNOMBRE, 

         IGAC_RESOLUCIONESDET.ANTTIPODOCUMENTO, 

         IGAC_RESOLUCIONES.ANO, 

         IGAC_RESOLUCIONESDET.FECHAINGRESOSISTEMA FECHA_CARGA

FROM  IP_IGAC_RESOLUCIONES IGAC_RESOLUCIONES 

  INNER JOIN  IP_IGAC_RESOLUCIONESDET  IGAC_RESOLUCIONESDET 

    ON  IGAC_RESOLUCIONES.COMPANIA     = IGAC_RESOLUCIONESDET.COMPANIA 

    AND IGAC_RESOLUCIONES.DEPARTAMENTO = IGAC_RESOLUCIONESDET.DEPARTAMENTO 

    AND IGAC_RESOLUCIONES.MUNICIPIO    = IGAC_RESOLUCIONESDET.MUNICIPIO 

    AND IGAC_RESOLUCIONES.ANO          = IGAC_RESOLUCIONESDET.ANO 

    AND IGAC_RESOLUCIONES.RESOLUCION   = IGAC_RESOLUCIONESDET.RESOLUCION 

WHERE IGAC_RESOLUCIONES.COMPANIA = s$compania$s

  AND TRUNC(IGAC_RESOLUCIONES.s$filtroFecha$s) BETWEEN TO_DATE('s$fechaInicial$s', 'DD/MM/YYYY')                
    
    AND TO_DATE('s$fechaFinal$s', 'DD/MM/YYYY')

GROUP BY IGAC_RESOLUCIONES.RESOLUCION, 

         IGAC_RESOLUCIONES.FECHAINGRESOSISTEMA, 

         IGAC_RESOLUCIONESDET.CONSECUTIVO, 

         IGAC_RESOLUCIONESDET.MUTACION, 

         IGAC_RESOLUCIONESDET.CODIGO, 

         IGAC_RESOLUCIONESDET.CANCELAINSCRIBE, 

         IGAC_RESOLUCIONESDET.TIPOREGISTRO, 

         IGAC_RESOLUCIONESDET.NUMEROORDEN, 

         IGAC_RESOLUCIONESDET.TOTALREGISTROS, 

         IGAC_RESOLUCIONESDET.NOMBRE, 

         IGAC_RESOLUCIONESDET.ESTADOCIVIL, 

         IGAC_RESOLUCIONESDET.TIPODOCUMENTO, 

         IGAC_RESOLUCIONESDET.NUMERODOCUMENTO, 

         IGAC_RESOLUCIONESDET.DIRECCION, 

         IGAC_RESOLUCIONESDET.DESTINOECONOMICO, 

         IGAC_RESOLUCIONESDET.AREATERRENO, 

         IGAC_RESOLUCIONESDET.AREACONSTRUIDA, 

         IGAC_RESOLUCIONESDET.AVALUO, 

         IGAC_RESOLUCIONESDET.VIGENCIA, 

         IGAC_RESOLUCIONESDET.CONDICIONTRIS, 

         IGAC_RESOLUCIONESDET.ANTRESOLUCION, 

         IGAC_RESOLUCIONESDET.ANTNOMBRE, 

         IGAC_RESOLUCIONESDET.ANTTIPODOCUMENTO, 

         IGAC_RESOLUCIONES.ANO, 

         IGAC_RESOLUCIONESDET.FECHAINGRESOSISTEMA

)

SELECT PREDIAL_LISIGACRESOLUCIONES.RESOLUCION,

       PREDIAL_LISIGACRESOLUCIONES.CODIGO,

       PREDIAL_LISIGACRESOLUCIONES.CANCELAINSCRIBE, 

       PREDIAL_LISIGACRESOLUCIONES.NUMEROORDEN, 

       PREDIAL_LISIGACRESOLUCIONES.AREACONSTRUIDA, 

       PREDIAL_LISIGACRESOLUCIONES.AVALUO,

       CASE WHEN PREDIAL_LISIGACRESOLUCIONES.NOMBRE

 IS NULL 

            THEN USUARIOS_PREDIAL.NOMBRE

            ELSE PREDIAL_LISIGACRESOLUCIONES.NOMBRE

            END NOMBRET, 

       CASE WHEN PREDIAL_LISIGACRESOLUCIONES.TIPODOCUMENTO

 IS NULL 

            THEN USUARIOS_PREDIAL.TIPO_NIT

            ELSE PREDIAL_LISIGACRESOLUCIONES.TIPODOCUMENTO

            END TIPODOCT, 

       CASE WHEN PREDIAL_LISIGACRESOLUCIONES.NUMERODOCUMENTO

 IS NULL 

            THEN USUARIOS_PREDIAL.NIT

            ELSE PREDIAL_LISIGACRESOLUCIONES.NUMERODOCUMENTO

            END NUMERODOCT, 

       CASE WHEN PREDIAL_LISIGACRESOLUCIONES.DIRECCION

 IS NULL 

            THEN USUARIOS_PREDIAL.DIRECCION

            ELSE PREDIAL_LISIGACRESOLUCIONES.DIRECCION

            END DIRECCIONT, 

       CASE WHEN PREDIAL_LISIGACRESOLUCIONES.TOTALREGISTROS

 IS NULL 

            THEN USUARIOS_PREDIAL.NUMERO_REGISTROS

            ELSE PREDIAL_LISIGACRESOLUCIONES.TOTALREGISTROS

            END TOTALREGIST, 

       CASE WHEN PREDIAL_LISIGACRESOLUCIONES.VIGENCIA

 IS NULL 

            THEN PREDIAL_LISIGACRESOLUCIONES.ANO

            ELSE PREDIAL_LISIGACRESOLUCIONES.VIGENCIA

            END VIGENCIAT

FROM PREDIAL_LISIGACRESOLUCIONES 

  INNER JOIN IP_USUARIOS_PREDIAL USUARIOS_PREDIAL 

    ON   PREDIAL_LISIGACRESOLUCIONES.NUMEROORDEN = USUARIOS_PREDIAL.NUMERO_ORDEN 

    AND PREDIAL_LISIGACRESOLUCIONES.CODIGO      = USUARIOS_PREDIAL.CODIGO
