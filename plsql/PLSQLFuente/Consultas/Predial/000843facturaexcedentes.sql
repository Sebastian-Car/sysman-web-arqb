SELECT 
  CODIGO_PREDIO,
  NOMBRE,
  FECHA_EXPEDICION,
  NUMERO_FACTURA,
  TO_CHAR(PCK_CODIGODEBARRAS.FC_IMPRIMIRCODIGODEBARRAS(CHR(205)||CHR(102)||'415'||'s$codigoEAN$s'||'8020'||SUBSTR(CODIGO_PREDIO,2,14)||SUBSTR(NUMERO_FACTURA,3,8)||'01'||'3900'||LPAD(s$valorFactura$s,14,'0')||'96'||TO_CHAR(FECHA_EXPEDICION,'YYYMMDD'))) AS CODIGODEBARRAS_BIN,
  CHR(40) || '415' || CHR(41) || 's$codigoEAN$s' || CHR(40) || '8020' || CHR(41) || SUBSTR(CODIGO_PREDIO,2,14)||SUBSTR(NUMERO_FACTURA,3,8)||'01'|| CHR(40) || '3900' || CHR(41) || LPAD(s$valorFactura$s,14,'0') || CHR(40) || '96' || CHR(41) || TO_CHAR(FECHA_EXPEDICION,'YYYMMDD') AS BARRAS
FROM IP_RECIBO_EXCEDENTES 
WHERE COMPANIA = s$compania$s
AND CODIGO_PREDIO = 's$predio$s'
AND ANULADO IN (0) 
 AND PAGO IN (0)  
