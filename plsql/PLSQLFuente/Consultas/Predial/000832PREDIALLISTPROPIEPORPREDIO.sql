SELECT   IP_USUARIOS_PREDIAL.CODIGO AS CODIGO_PREDIO, 
          IP_USUARIOS_PREDIAL.NUMERO_ORDEN, 
          IP_USUARIOS_PREDIAL.NIT AS NIT_PROPIETARIO, 
          IP_USUARIOS_PREDIAL.NOMBRE AS NOMBRE_PROPIETARIO,  
          IP_USUARIOS_PREDIAL.PREDCO, 
          IP_USUARIOS_PREDIAL.DIRECCION, 
          IP_USUARIOS_PREDIAL.AVALUO_ANO AS AVALUO, 
          IP_USUARIOS_PREDIAL.AREA_HA AS HECTAREAS_METROS,  
          IP_USUARIOS_PREDIAL.AREA_CONSTRUIDA AS AREA_CONSTRUIDA, 
          IP_USUARIOS_PREDIAL.M_INMOBILIARIA AS MATRICULA_INMOBILIARIA,
          CASE WHEN IP_USUARIOS_PREDIAL.BLOQUEADO NOT IN (0)
               THEN 'SI'
               ELSE 'NO'
          END PREDIO_BLOQUEADO  
FROM IP_USUARIOS_PREDIAL  
WHERE IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s
  AND IP_USUARIOS_PREDIAL.CODIGO BETWEEN s$codigoInicial$s AND s$codigoFinal$s  
  s$condBloqueado$s  
  AND IP_USUARIOS_PREDIAL.CODIGO_NO_ACTIVO IN (0)
  AND IP_USUARIOS_PREDIAL.INDBORRADO IN (0)
  s$condicion$s
  s$ordenado$s
