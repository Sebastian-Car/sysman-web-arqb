SELECT IP_NOTIFICAPREDIAL.CONSECUTIVO,
  IP_NOTIFICAPREDIAL.CODIGO,
  IP_NOTIFICAPREDIAL.NIT,
  TO_CHAR(IP_NOTIFICAPREDIAL.FECHA_REGISTRO,'DD/MM/YYYY') FECHA_REGISTRO,
  IP_NOTIFICAPREDIAL.NOMBRE,
  IP_USUARIOS_PREDIAL.DIRECCION,
  IP_USUARIOS_PREDIAL.TELEFONO_PROPIETARIO,
  IP_NOTIFICAPREDIAL.ANOFIN PREANO,
  IP_NOTIFICAPREDIAL.TOTAL
FROM IP_NOTIFICAPREDIAL
INNER JOIN IP_USUARIOS_PREDIAL
ON IP_NOTIFICAPREDIAL.COMPANIA       = IP_USUARIOS_PREDIAL.COMPANIA
AND IP_NOTIFICAPREDIAL.CODIGO        = IP_USUARIOS_PREDIAL.CODIGO
AND IP_NOTIFICAPREDIAL.NUMERO_ORDEN  = IP_USUARIOS_PREDIAL.NUMERO_ORDEN
WHERE IP_NOTIFICAPREDIAL.COMPANIA    = s$compania$s
AND IP_NOTIFICAPREDIAL.NUMERO_ORDEN  = 's$numeroOrden$s'
AND IP_USUARIOS_PREDIAL.INDBORRADO  IN (0)
AND IP_USUARIOS_PREDIAL.CODIGO_NO_ACTIVO  IN (0)
s$condAdicional$s 
ORDER BY IP_NOTIFICAPREDIAL.CODIGO,
  IP_NOTIFICAPREDIAL.CONSECUTIVO
