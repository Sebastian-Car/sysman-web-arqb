SELECT IP_AUDITORIA.MODCOD,
  IP_AUDITORIA.MODOPE,
  IP_AUDITORIA.MODFEC,
  IP_AUDITORIA.MODHOR,
  IP_AUDITORIA.MODCCO,
  IP_MODICOD.RMODES,
  IP_AUDITORIA.MODVAN,
  IP_AUDITORIA.MODVNU,
  IP_AUDITORIA.DESCRIPCION
FROM IP_AUDITORIA
INNER JOIN IP_MODICOD
ON IP_AUDITORIA.MODCCO = IP_MODICOD.RMOCOD 
 WHERE s$condicion$s 
ORDER BY IP_AUDITORIA.MODFEC,
         IP_AUDITORIA.MODHOR
