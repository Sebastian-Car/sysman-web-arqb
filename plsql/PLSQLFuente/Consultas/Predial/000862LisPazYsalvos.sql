SELECT 
  COMPANIA,
  NUMCER,
  CODIGOPREDIO,
  CASE WHEN ANULADO NOT IN (0) THEN 'X' ELSE ' ' END ANULADO,
  NOMBRE,
  CASE WHEN ANULADO NOT IN (0) THEN 0 ELSE VALOR END VLR
FROM IP_CERTIFICADOPS
WHERE COMPANIA = s$compania$s
  AND TRUNC(FECHA_EXP) BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
  s$conAnulados$s
