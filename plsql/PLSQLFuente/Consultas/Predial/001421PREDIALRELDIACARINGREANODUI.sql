SELECT DISTINCT IP_RECIBOS_DE_PAGO.DOCNUM, 
IP_RECIBOS_DE_PAGO.PREANO, 
IP_USUARIOS_PREDIAL.NOMBRE, 
IP_RECIBOS_DE_PAGO.AVALUO, 
IP_RECIBOS_DE_PAGO.PAGO, 
IP_RECIBOS_DE_PAGO.PREFECPAG, 
IP_RECIBOS_DE_PAGO.C1 AS C1, 
IP_RECIBOS_DE_PAGO.C2 AS C2, 
IP_RECIBOS_DE_PAGO.C3 AS C3, 
IP_RECIBOS_DE_PAGO.C5 AS C5, 
IP_RECIBOS_DE_PAGO.C6 AS C6,
IP_RECIBOS_DE_PAGO.C7 AS C7,
IP_RECIBOS_DE_PAGO.C8 AS C8,   
IP_RECIBOS_DE_PAGO.C9 AS C9, 
IP_RECIBOS_DE_PAGO.C10 AS C10,
IP_RECIBOS_DE_PAGO.C11 AS C11,  
IP_RECIBOS_DE_PAGO.C13 AS C13, 
IP_RECIBOS_DE_PAGO.C14 AS C14, 
IP_RECIBOS_DE_PAGO.C15 AS C15, 
IP_RECIBOS_DE_PAGO.C16 AS C16, 
IP_RECIBOS_DE_PAGO.C17 AS C17, 
IP_RECIBOS_DE_PAGO.C18 AS C18, 
IP_RECIBOS_DE_PAGO.C19 AS C19,  
IP_RECIBOS_DE_PAGO.C20 AS C20,  
IP_RECIBOS_DE_PAGO.C13 AS descuento_predial,
IP_RECIBOS_DE_PAGO.C13 AS descuento_carl,
IP_RECIBOS_DE_PAGO.PAG_BANPAG AS PAG_BAN, 
IP_BANCOS.NOMBREBANCO, 
IP_RECIBOS_DE_PAGO.PRECOD AS CODIGO, 
TRUNC(IP_RECIBOS_DE_PAGO.INTERES_ACUERDO) AS INT_ACUERDO, 
TRUNC(IP_RECIBOS_DE_PAGO.INTERES_RECARGO) AS INT_RECARGO, 
IP_RECIBOS_DE_PAGO.PREVAL AS ValorFacturado
FROM (IP_RECIBOS_DE_PAGO
	LEFT JOIN IP_USUARIOS_PREDIAL 
		ON IP_RECIBOS_DE_PAGO.COMPANIA = IP_USUARIOS_PREDIAL.COMPANIA
		AND IP_RECIBOS_DE_PAGO.PRECOD = IP_USUARIOS_PREDIAL.CODIGO) 
			LEFT JOIN IP_BANCOS 
			ON IP_RECIBOS_DE_PAGO.COMPANIA = IP_BANCOS.COMPANIA
			AND IP_RECIBOS_DE_PAGO.PAG_BANPAG = IP_BANCOS.CODIGOBANCO
WHERE IP_RECIBOS_DE_PAGO.COMPANIA = s$compania$s
	AND IP_RECIBOS_DE_PAGO.PAGO=-1 
	AND IP_RECIBOS_DE_PAGO.PREFECPAG Between s$fechaInicio$s And s$fechaFinal$s 
	AND IP_RECIBOS_DE_PAGO.PAG_BANPAG Between s$bancoInicio$s And s$bancoFinal$s 
	AND IP_RECIBOS_DE_PAGO.ANULADO=0 
	AND IP_RECIBOS_DE_PAGO.NUMERO_ORDEN=s$nOrden$s
	ORDER BY IP_RECIBOS_DE_PAGO.DOCNUM
