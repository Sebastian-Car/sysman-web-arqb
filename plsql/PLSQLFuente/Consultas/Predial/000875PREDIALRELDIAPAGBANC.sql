SELECT IP_RECIBOS_DE_PAGO.DOCNUM,
    IP_USUARIOS_PREDIAL.CODIGO,
    IP_RECIBOS_DE_PAGO.PREANO AS ANO,
    IP_USUARIOS_PREDIAL.NOMBRE,
    IP_BANCOS.NOMBREBANCO,
    IP_RECIBOS_DE_PAGO.PREFECPAG AS PREFEC,
    IP_PAGO_BANCOSCAB.PAG_BAN,
    IP_PAGO_BANCOSCAB.NROCUPONES,
    IP_PAGO_BANCOSCAB.VLRREPORTADO,
    IP_PAGO_BANCOSCAB.NROCUPONESACU,
    IP_PAGO_BANCOSCAB.ACUMULADO,
    IP_PAGO_BANCOSCAB.PAQUETE,
    IP_RECIBOS_DE_PAGO.PREVAL
FROM IP_RECIBOS_DE_PAGO INNER JOIN IP_USUARIOS_PREDIAL ON 
        IP_RECIBOS_DE_PAGO.COMPANIA = IP_USUARIOS_PREDIAL.COMPANIA 
        AND IP_RECIBOS_DE_PAGO.NUMERO_ORDEN = IP_USUARIOS_PREDIAL.NUMERO_ORDEN 
        AND IP_RECIBOS_DE_PAGO.PRECOD = IP_USUARIOS_PREDIAL.CODIGO 
    INNER JOIN IP_PAGO_BANCOSCAB ON 
        IP_RECIBOS_DE_PAGO.COMPANIA = IP_PAGO_BANCOSCAB.COMPANIA 
        AND IP_RECIBOS_DE_PAGO.PAQUETEPAG  = IP_PAGO_BANCOSCAB.PAQUETE 
        AND IP_RECIBOS_DE_PAGO.PREFECPAG  = IP_PAGO_BANCOSCAB.PREFEC 
        AND IP_RECIBOS_DE_PAGO.PAG_BANPAG = IP_PAGO_BANCOSCAB.PAG_BAN 
    INNER JOIN IP_BANCOS ON 
        IP_PAGO_BANCOSCAB.COMPANIA = IP_BANCOS.COMPANIA
        AND IP_PAGO_BANCOSCAB.PAG_BAN = IP_BANCOS.CODIGOBANCO
WHERE IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s 
    AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN = 's$numeroOrden$s' 
    AND IP_RECIBOS_DE_PAGO.PREFECPAG BETWEEN s$fechaInicial$s AND s$fechaFinal$s 
    AND IP_PAGO_BANCOSCAB.PAG_BAN BETWEEN s$bancoInicial$s AND s$bancoFinal$s 
    AND IP_RECIBOS_DE_PAGO.PAGO NOT IN (0) 
    AND IP_RECIBOS_DE_PAGO.ANULADO IN (0)
