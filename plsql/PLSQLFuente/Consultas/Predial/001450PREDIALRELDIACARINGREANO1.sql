SELECT DISTINCT TO_CHAR(IP_RECIBOS_DE_PAGO.PREFECPAG, 'DD/MM/YYYY') FECHAPREFECPAG,
  DECODE(IP_RECIBOS_DE_PAGO.ESACUERDO, '0', 'Corriente', '-1', 'Acuerdos de Pago') ESACUERDO,
  IP_RECIBOS_DE_PAGO.DOCNUM,
  IP_RECIBOS_DE_PAGO.PRECOD,
  IP_USUARIOS_PREDIAL.NOMBRE,
  IP_BANCOS.NOMBREBANCO,
  IP_RECIBOS_DE_PAGO.AVALUO,
  IP_RECIBOS_DE_PAGO.ANULADO,
  IP_RECIBOS_DE_PAGO.PAGO,
  IP_RECIBOS_DE_PAGO.PAG_BANPAG,
  IP_RECIBOS_DE_PAGO.PREFECPAG,                                                                                                                                                                                                                                                                          
  IP_RECIBOS_DE_PAGO.C1 , 
  IP_RECIBOS_DE_PAGO.C13 ,
  IP_RECIBOS_DE_PAGO.C5 +IP_RECIBOS_DE_PAGO.C9  AS SUMA1,  
  IP_RECIBOS_DE_PAGO.C2 ,                                
  IP_RECIBOS_DE_PAGO.C6 + IP_RECIBOS_DE_PAGO.C10  AS SUMA2,
  IP_RECIBOS_DE_PAGO.C1+IP_RECIBOS_DE_PAGO.C2+IP_RECIBOS_DE_PAGO.C5+IP_RECIBOS_DE_PAGO.C6+IP_RECIBOS_DE_PAGO.C9+IP_RECIBOS_DE_PAGO.C10+IP_RECIBOS_DE_PAGO.C13 AS SUMA3,
  IP_RECIBOS_DE_PAGO.C14+IP_RECIBOS_DE_PAGO.C15+IP_RECIBOS_DE_PAGO.C16+IP_RECIBOS_DE_PAGO.C17+IP_RECIBOS_DE_PAGO.C18+IP_RECIBOS_DE_PAGO.C19- s$descAparte$s +IP_RECIBOS_DE_PAGO.C20 AS SUMA4,
  IP_RECIBOS_DE_PAGO.C3, 
  NVL(s$descAparte$s,0) AS DESCAPARTE,
  IP_RECIBOS_DE_PAGO.C7 + IP_RECIBOS_DE_PAGO.C11 AS SUMA5,
  IP_RECIBOS_DE_PAGO.C4,
  IP_RECIBOS_DE_PAGO.C8+IP_RECIBOS_DE_PAGO.C12 AS SUMA6,
  IP_RECIBOS_DE_PAGO.C3+IP_RECIBOS_DE_PAGO.C4+IP_RECIBOS_DE_PAGO.C7+IP_RECIBOS_DE_PAGO.C8 AS SUMA7,
  NVL(IP_RECIBOS_DE_PAGO.INTERES_ACUERDO,0) AS INT_ACUERDO,
  NVL(IP_RECIBOS_DE_PAGO.INTERES_RECARGO,0) AS INT_RECARGO,
    IP_RECIBOS_DE_PAGO.C1+IP_RECIBOS_DE_PAGO.C2+IP_RECIBOS_DE_PAGO.C5+IP_RECIBOS_DE_PAGO.C6+IP_RECIBOS_DE_PAGO.C9+IP_RECIBOS_DE_PAGO.C10+IP_RECIBOS_DE_PAGO.c13+IP_RECIBOS_DE_PAGO.C14+IP_RECIBOS_DE_PAGO.C15+IP_RECIBOS_DE_PAGO.C16+IP_RECIBOS_DE_PAGO.C17+IP_RECIBOS_DE_PAGO.C18+IP_RECIBOS_DE_PAGO.C19+ s$descAparte$s +IP_RECIBOS_DE_PAGO.C3+IP_RECIBOS_DE_PAGO.C4+IP_RECIBOS_DE_PAGO.C7+IP_RECIBOS_DE_PAGO.C8+IP_RECIBOS_DE_PAGO.C11+IP_RECIBOS_DE_PAGO.C12+ NVL(IP_RECIBOS_DE_PAGO.INTERES_ACUERDO,0)+NVL(IP_RECIBOS_DE_PAGO.INTERES_RECARGO,0)- s$descAparte$s +IP_RECIBOS_DE_PAGO.C20 AS VALORFACTURADO  
FROM IP_RECIBOS_DE_PAGO
   INNER JOIN IP_USUARIOS_PREDIAL   
   ON IP_RECIBOS_DE_PAGO.COMPANIA      = IP_USUARIOS_PREDIAL.COMPANIA
   AND IP_RECIBOS_DE_PAGO.PRECOD       = IP_USUARIOS_PREDIAL.CODIGO
   AND IP_RECIBOS_DE_PAGO.NUMERO_ORDEN = IP_USUARIOS_PREDIAL.NUMERO_ORDEN
INNER JOIN IP_BANCOS
   ON IP_RECIBOS_DE_PAGO.COMPANIA    = IP_BANCOS.COMPANIA
   AND IP_RECIBOS_DE_PAGO.PAG_BANPAG = IP_BANCOS.CODIGOBANCO
WHERE IP_RECIBOS_DE_PAGO.COMPANIA = s$compania$s
  AND IP_RECIBOS_DE_PAGO.ANULADO    IN(0)
  AND IP_RECIBOS_DE_PAGO.PAGO NOT     IN (0)
  AND IP_RECIBOS_DE_PAGO.PREFECPAG BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND IP_RECIBOS_DE_PAGO.PAG_BANPAG BETWEEN 's$codigoBancoInicial$s' AND 's$codigoBancoFinal$s'
  AND IP_RECIBOS_DE_PAGO.PAG_BANPAG  NOT IN('s$prcodbanccompen$s')
  AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN = 's$numeroOrden$s' 
  AND IP_RECIBOS_DE_PAGO.PAQUETEPAG BETWEEN 's$cmbPaqIni$s' AND 's$cmbPaqFin$s'
ORDER BY IP_RECIBOS_DE_PAGO.DOCNUM
