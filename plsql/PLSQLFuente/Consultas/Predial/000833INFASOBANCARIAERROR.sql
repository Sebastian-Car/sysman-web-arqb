SELECT 
  IP_ASOBANCARIA_LOG.FECHARECAUDO,
  IP_ASOBANCARIA_LOG.FECHA, 
  IP_ASOBANCARIA_ERROR.ID, 
  IP_BANCOS.NOMBREBANCO BANCO,
  IP_ASOBANCARIA_ERROR.CODIGO, 
  IP_ASOBANCARIA_ERROR.FACTURA, 
  IP_ASOBANCARIA_ERROR.DESCRIPCION 
FROM IP_ASOBANCARIA_ERROR 
  INNER JOIN IP_ASOBANCARIA_LOG 
     ON IP_ASOBANCARIA_ERROR.COMPANIA = IP_ASOBANCARIA_LOG.COMPANIA 
    AND IP_ASOBANCARIA_ERROR.ID = IP_ASOBANCARIA_LOG.ID 
  INNER JOIN IP_BANCOS
     ON IP_ASOBANCARIA_LOG.COMPANIA = IP_BANCOS.COMPANIA 
    AND IP_ASOBANCARIA_LOG.BANCO = IP_BANCOS.CODIGOBANCO 
WHERE IP_ASOBANCARIA_ERROR.COMPANIA = s$compania$s
  AND s$fechaCampo$s = s$fecha$s
  AND IP_ASOBANCARIA_LOG.BANCO = 's$banco$s'
GROUP BY IP_ASOBANCARIA_LOG.FECHARECAUDO, IP_ASOBANCARIA_LOG.FECHA, IP_ASOBANCARIA_ERROR.ID, 
  IP_BANCOS.NOMBREBANCO, IP_ASOBANCARIA_ERROR.CODIGO, IP_ASOBANCARIA_ERROR.FACTURA, IP_ASOBANCARIA_ERROR.DESCRIPCION
