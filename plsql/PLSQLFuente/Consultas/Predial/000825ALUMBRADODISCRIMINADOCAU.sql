WITH  CAUSACION AS (
SELECT 
  SUM(CASE WHEN DR.PREANO = TO_CHAR(SYSDATE, 'YYYY') THEN NVL(DR.Cs$concepto$s, 0) ELSE 0 END) ALUMBRADO_ACTUAL, 
  SUM(CASE WHEN DR.PREANO = TO_CHAR(SYSDATE, 'YYYY') -1 THEN NVL(DR.Cs$concepto$s, 0) ELSE 0 END) ALUMBRADO_ANTERIOR, 
  SUM(CASE WHEN DR.PREANO < TO_CHAR(SYSDATE, 'YYYY') -1 THEN NVL(DR.Cs$concepto$s, 0) ELSE 0 END) ALUMBRADO_DIFRECAUDO 
FROM IP_DETALLE_RECIBOPAGO DR
  INNER JOIN IP_RECIBOS_DE_PAGO RP
  ON  DR.COMPANIA = RP.COMPANIA
  AND DR.DOCNUM = RP.DOCNUM
WHERE DR.COMPANIA = 's$compania$s'
--AND   RP.ANULADO IN(0)
AND   TRUNC(RP.PREFEC) = s$fechaFinal$s
UNION 
SELECT 
  SUM(CASE WHEN DR.PREANO = TO_CHAR(SYSDATE, 'YYYY') THEN NVL(DR.Cs$concepto$s, 0) *-1 ELSE 0 END) ALUMBRADO_ACTUAL, 
  SUM(CASE WHEN DR.PREANO = TO_CHAR(SYSDATE, 'YYYY') -1 THEN NVL(DR.Cs$concepto$s, 0) *-1 ELSE 0 END) ALUMBRADO_ANTERIOR, 
  SUM(CASE WHEN DR.PREANO < TO_CHAR(SYSDATE, 'YYYY') -1 THEN NVL(DR.Cs$concepto$s, 0) *-1 ELSE 0 END) ALUMBRADO_DIFRECAUDO 
FROM IP_DETALLE_RECIBOPAGO DR
  INNER JOIN IP_RECIBOS_DE_PAGO RP
  ON  DR.COMPANIA = RP.COMPANIA
  AND DR.DOCNUM = RP.DOCNUM
WHERE DR.COMPANIA = 's$compania$s'
AND   RP.ANULADO NOT IN(0)
AND   TRUNC(RP.FECHAANULACION) = s$fechaFinal$s )
SELECT 
	SUM(ALUMBRADO_ACTUAL) AS ALUMACT, 
	SUM(ALUMBRADO_ANTERIOR) AS ALUMANT, 
	SUM(ALUMBRADO_DIFRECAUDO) AS ALUMANTS
FROM CAUSACION
