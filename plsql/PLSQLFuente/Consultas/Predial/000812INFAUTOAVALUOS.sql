SELECT  IP_USUARIOS_PREDIAL.COMPANIA,
        IP_USUARIOS_PREDIAL.CODIGO,
        IP_USUARIOS_PREDIAL.M_INMOBILIARIA, 
        IP_USUARIOS_PREDIAL.NOMBRE, 
        IP_USUARIOS_PREDIAL.DIRECCION, 
        IP_AUTOAVALUO.AUTOAVALUO ,
        IP_AUTOAVALUO.AVALUO_ANT, 
        IP_AUTOAVALUO.DATE_CREATED, 
        IP_AUTOAVALUO.HORA_CREA_REGISTRO
FROM IP_AUTOAVALUO 
  INNER JOIN IP_USUARIOS_PREDIAL 
    ON IP_AUTOAVALUO.COMPANIA = IP_USUARIOS_PREDIAL.COMPANIA 
    AND IP_AUTOAVALUO.CODPRE = IP_USUARIOS_PREDIAL.CODIGO
    AND IP_AUTOAVALUO.NUMERO_ORDEN = IP_USUARIOS_PREDIAL.NUMERO_ORDEN
WHERE IP_AUTOAVALUO.COMPANIA = s$compania$s
   AND  IP_USUARIOS_PREDIAL.CODIGO BETWEEN s$codigoInicial$s AND s$codigoFinal$s 
   AND IP_AUTOAVALUO.DATE_CREATED BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  s$condicion$s
