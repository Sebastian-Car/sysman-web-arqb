SELECT FACTURADOS.CODIGO          AS PREDIO,
  USUARIOS_PREDIAL.M_INMOBILIARIA AS NO_MATRICULA,
  NIT,
  TO_CHAR(USUARIOS_PREDIAL.PAGO_ANO)       AS ULTIMO_ANOPAGO,
  NOMBRE,
  DIRECCION,
  s$vigencias$s,
  SUM (s$vigCap$s)                AS TOTAL_CAPITAL,
  SUM(s$vigInt$s)                 AS TOTAL_INTERES,
  SUM(FACTURADOS.TOTAL)           AS TOTAL_GENERAL
  FROM IP_FACTURADOS FACTURADOS
INNER JOIN IP_USUARIOS_PREDIAL USUARIOS_PREDIAL
ON FACTURADOS.COMPANIA      = USUARIOS_PREDIAL.COMPANIA
AND FACTURADOS.CODIGO       = USUARIOS_PREDIAL.CODIGO
AND FACTURADOS.NUMERO_ORDEN = USUARIOS_PREDIAL.NUMERO_ORDEN
WHERE USUARIOS_PREDIAL.NUMERO_ORDEN = s$numerOrden$s AND FACTURADOS.PAGADO IN(0)  
 AND USUARIOS_PREDIAL.COMPANIA=s$compania$s
 AND (FACTURADOS.INDCAR IN (0) OR (FACTURADOS.INDCAR) IS NULL)
 AND (FACTURADOS.INDEXE IN(0) OR (FACTURADOS.INDEXE) IS NULL)
 AND (FACTURADOS.INDEXEOTROS IN(0)  OR FACTURADOS.INDEXEOTROS IS NULL)
 AND (FACTURADOS.NOCOBRADO IN(0) OR FACTURADOS.NOCOBRADO IS NULL)
 AND FACTURADOS.INDPAGO_ACPAG IN(0) AND (FACTURADOS.INDPAGO_CUOTAS IN(0) OR FACTURADOS.INDPAGO_CUOTAS IS NULL)
 AND USUARIOS_PREDIAL.CODIGO BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
 s$strIncluye$s
GROUP BY FACTURADOS.CODIGO,
  USUARIOS_PREDIAL.M_INMOBILIARIA,
  NIT,
  USUARIOS_PREDIAL.PAGO_ANO,
  NOMBRE ,
  DIRECCION
