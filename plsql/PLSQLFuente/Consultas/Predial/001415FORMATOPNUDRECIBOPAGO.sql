WITH TOTALDETALLE AS
(  
  SELECT FACTURADOS.COMPANIA,
         FACTURADOS.CODIGO, 
         FACTURADOS.NUMERO_ORDEN, 
         SUM(FACTURADOS.TOTAL) AS TOTAL
   FROM IP_FACTURADOS FACTURADOS
  WHERE FACTURADOS.COMPANIA                 = s$compania$s
       AND FACTURADOS.CODIGO                     = s$codigoPredial$s
       AND FACTURADOS.PAGADO                     IN(0)
       AND FACTURADOS.INDPAGO_ACPAG      IN(0)
       AND (s$anoUnico$s IN(0)  AND FACTURADOS.PREANO <= s$anoFin$s)
        OR (s$anoUnico$s IN(-1) AND FACTURADOS.PREANO  = s$anoFin$s)
  GROUP BY COMPANIA,
        CODIGO,
        NUMERO_ORDEN
)
SELECT USUARIOS_PREDIAL.CODIGO,
        USUARIOS_PREDIAL.NUMERO_ORDEN,
        USUARIOS_PREDIAL.NIT,
        USUARIOS_PREDIAL.NUM_COM,
        USUARIOS_PREDIAL.AREA_HA,
        USUARIOS_PREDIAL.AREA_M2,
        USUARIOS_PREDIAL.AREA_CONSTRUIDA,
        USUARIOS_PREDIAL.NOMBRE,
        USUARIOS_PREDIAL.DIRECCION,
        USUARIOS_PREDIAL.DIRECCION_CORRESPONDENCIA,
        USUARIOS_PREDIAL.PAGO_ANO,
        TO_CHAR(USUARIOS_PREDIAL.PAG_FEC,'DD/MM/YYYY') PAG_FEC,
        USUARIOS_PREDIAL.PAG_VAL, 
        TOTALDETALLE.TOTAL
FROM IP_FACTURADOS FACTURADOS
  INNER JOIN IP_USUARIOS_PREDIAL USUARIOS_PREDIAL
     ON FACTURADOS.COMPANIA   	  = USUARIOS_PREDIAL.COMPANIA
    AND FACTURADOS.CODIGO      	 = USUARIOS_PREDIAL.CODIGO 
    AND FACTURADOS.NUMERO_ORDEN = USUARIOS_PREDIAL.NUMERO_ORDEN  
  INNER JOIN TOTALDETALLE 
     ON FACTURADOS.COMPANIA     	  = TOTALDETALLE.COMPANIA
    AND FACTURADOS.CODIGO       	  = TOTALDETALLE.CODIGO 
    AND FACTURADOS.NUMERO_ORDEN  = TOTALDETALLE.NUMERO_ORDEN        
WHERE FACTURADOS.COMPANIA                    = s$compania$s
     AND FACTURADOS.CODIGO                        = s$codigoPredial$s
     AND FACTURADOS.NUMERO_ORDEN          = s$numeroOrden$s
     AND FACTURADOS.PREANO BETWEEN s$pagoAno$s + 1 AND 9999
     AND (s$anoUnico$s IN(0)  AND FACTURADOS.PREANO <= s$anoFin$s)
      OR (s$anoUnico$s IN(-1) AND FACTURADOS.PREANO  = s$anoFin$s)
GROUP BY USUARIOS_PREDIAL.CODIGO,
        USUARIOS_PREDIAL.NUMERO_ORDEN,
        USUARIOS_PREDIAL.NIT,
        USUARIOS_PREDIAL.NUM_COM,
        USUARIOS_PREDIAL.AREA_HA,
        USUARIOS_PREDIAL.AREA_M2,
        USUARIOS_PREDIAL.AREA_CONSTRUIDA,
        USUARIOS_PREDIAL.NOMBRE,
        USUARIOS_PREDIAL.DIRECCION,
        USUARIOS_PREDIAL.DIRECCION_CORRESPONDENCIA,
        USUARIOS_PREDIAL.PAGO_ANO,
        USUARIOS_PREDIAL.PAG_FEC,
        USUARIOS_PREDIAL.PAG_VAL,
        TOTALDETALLE.TOTAL
