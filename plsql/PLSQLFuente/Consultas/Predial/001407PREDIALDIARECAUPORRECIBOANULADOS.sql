  AND USUARIOS_PREDIAL.NUMERO_ORDEN  = 's$numeroOrden$s'  
  AND RECIBOS_DE_PAGO.FECHAANULACION BETWEEN s$fechaInicial$s AND s$fechaFinal$s 
  AND RECIBOS_DE_PAGO.ANULADO        NOT IN (0) 
