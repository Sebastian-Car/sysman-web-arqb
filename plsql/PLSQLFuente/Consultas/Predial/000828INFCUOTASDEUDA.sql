SELECT   IP_ACUERDOS.COMPANIA,
          IP_ACUERDOS.CODIGOACUERDO, 
          IP_ACUERDOS.PREDIO, 
          IP_ACUERDOS.ID_RESP, 
          IP_ACUERDOS.NOMBRE_RESP, 
          IP_ACUERDOS.DIRECCION_RESP, 
          IP_ACUERDOS.TELEFONO_RESP, 
          MAX(IP_FACTURADOSACUERDOS.CUOTA)  CUOTAS_PACTADAS, 
          COUNT(IP_FACTURADOSACUERDOS.CUOTA)  CUOTAS_DEUDA, 
          IP_ACUERDOS.RESOLUCION, 
          NVL(SUM(IP_FACTURADOSACUERDOS.CAPITAL), 0)  TCAPITAL, 
          NVL(SUM(IP_FACTURADOSACUERDOS.INTERESES), 0)  TINTERESES, 
          NVL(SUM(IP_FACTURADOSACUERDOS.INTERES_ACUERDO), 0)  TINTERES_ACUERDO, 
          NVL(SUM(IP_FACTURADOSACUERDOS.INTERES_RECARGO), 0)  TINTERES_RECARGO
  FROM    IP_ACUERDOS 
  INNER JOIN IP_FACTURADOSACUERDOS  
  ON      IP_ACUERDOS.COMPANIA      = IP_FACTURADOSACUERDOS.COMPANIA 
  AND     IP_ACUERDOS.PREDIO        = IP_FACTURADOSACUERDOS.PREDIO 
  AND     IP_ACUERDOS.CODIGOACUERDO = IP_FACTURADOSACUERDOS.CODIGOACUERDO
  AND     IP_ACUERDOS.NUMERO_ORDEN  = IP_FACTURADOSACUERDOS.NUMERO_ORDEN
  INNER JOIN IP_USUARIOS_PREDIAL 
  ON      IP_ACUERDOS.COMPANIA      = IP_USUARIOS_PREDIAL.COMPANIA 
  AND     IP_ACUERDOS.PREDIO        = IP_USUARIOS_PREDIAL.CODIGO 
  AND     IP_ACUERDOS.NUMERO_ORDEN  = IP_USUARIOS_PREDIAL.NUMERO_ORDEN 
  WHERE IP_ACUERDOS.COMPANIA = s$compania$s
  AND   IP_ACUERDOS.ANULADO = 0
  AND   IP_ACUERDOS.CANCELADO = 0 
  AND   IP_FACTURADOSACUERDOS.PAGADO = 0
  AND   IP_USUARIOS_PREDIAL.NUMERO_ORDEN = s$numeroOrden$s
  AND   IP_USUARIOS_PREDIAL.INDCAR = 0
  AND   IP_USUARIOS_PREDIAL.INDEXE = 0 
  AND   IP_USUARIOS_PREDIAL.INDBORRADO = 0
  AND   IP_USUARIOS_PREDIAL.CODIGO_NO_ACTIVO = 0
  AND   IP_ACUERDOS.FECHAACUERDO BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND   IP_ACUERDOS.CODIGOACUERDO BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
  GROUP BY 
      IP_ACUERDOS.COMPANIA,
      IP_ACUERDOS.CODIGOACUERDO, 
      IP_ACUERDOS.PREDIO, 
      IP_ACUERDOS.ID_RESP, 
      IP_ACUERDOS.NOMBRE_RESP, 
      IP_ACUERDOS.DIRECCION_RESP, 
      IP_ACUERDOS.TELEFONO_RESP, 
      IP_ACUERDOS.RESOLUCION
