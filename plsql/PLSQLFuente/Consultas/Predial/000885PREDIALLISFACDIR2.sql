SELECT CSTD.CODIGO, 
       U.NOMBRE, 
       SC1+SC2+SC3+SC4+SC13+SC14+SC15+SC16+SC17+SC18+SC19+SC20 TOTAL, 
       CSTD.SC1 C1, 
       CSTD.SC2 C2, 
       CSTD.SC3 C3, 
       CSTD.SC4 C4, 
       CSTD.SC13 C13, 
       SC14+SC15+SC16+SC17+SC18+SC19+SC20 OTROS, 
       U.M_INMOBILIARIA
