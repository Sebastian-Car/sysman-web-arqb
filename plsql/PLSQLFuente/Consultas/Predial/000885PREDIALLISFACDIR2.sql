  INNER JOIN CNS_CARTERA_STD_TV CSTD 
    ON  U.COMPANIA = CSTD.COMPANIA
    AND U.CODIGO   = CSTD.CODIGO 
WHERE U.NUMERO_ORDEN     = 's$numeroOrden$s' 
  AND U.COMPANIA         = s$compania$s 
  AND U.DIRECCION  BETWEEN 's$direccionInicial$s' 
                                                AND 's$direccionFinal$s'  
  AND CSTD.STOTAL        > s$masDeCuanto$s
  AND U.INDCAR           IN (0)
  AND U.INDEXE           IN (0)
  AND U.INDBORRADO       IN (0)
  AND U.CODIGO_NO_ACTIVO IN (0)
GROUP BY CSTD.CODIGO, 
       U.NOMBRE, 
       SC1+SC2+SC3+SC4+SC13+SC14+SC15+SC16+SC17+SC18+SC19+SC20, 
       CSTD.SC1, 
       CSTD.SC2, 
       CSTD.SC3, 
       CSTD.SC4, 
       CSTD.SC13, 
       SC14+SC15+SC16+SC17+SC18+SC19+SC20, 
       U.M_INMOBILIARIA
)
WHERE s$conAcuerdos$s = 2
