SELECT IP_FACTURADOS.PREANO,
       s$cantidadPredios$s
       SUM(IP_FACTURADOS.C1)  IMP, 
       SUM(IP_FACTURADOS.C2)  INTIMP, 
       SUM(IP_FACTURADOS.C3)  CAR, 
       SUM(IP_FACTURADOS.C4)  INTCAR, 
       SUM(IP_FACTURADOS.C13) C13, 
       SUM(IP_FACTURADOS.C14)  C14, 
       SUM(IP_FACTURADOS.C15)  C15, 
       SUM(IP_FACTURADOS.C16)  C16, 
       SUM(IP_FACTURADOS.C17)  C17, 
       SUM(IP_FACTURADOS.C18)  C18, 
       SUM(IP_FACTURADOS.C19)  C19, 
       SUM(IP_FACTURADOS.C20)  C20, 
       SUM(IP_FACTURADOS.C1 + IP_FACTURADOS.C2 + IP_FACTURADOS.C3 + IP_FACTURADOS.C4 + IP_FACTURADOS.C13 + IP_FACTURADOS.C14 + IP_FACTURADOS.C15 + IP_FACTURADOS.C16 + IP_FACTURADOS.C17 + IP_FACTURADOS.C18 + IP_FACTURADOS.C19 + IP_FACTURADOS.C20)  TOTAL 
FROM   IP_USUARIOS_PREDIAL 
INNER JOIN IP_FACTURADOS 
ON     IP_USUARIOS_PREDIAL.COMPANIA = IP_FACTURADOS.COMPANIA
AND    IP_USUARIOS_PREDIAL.NUMERO_ORDEN = IP_FACTURADOS.NUMERO_ORDEN 
AND    IP_USUARIOS_PREDIAL.CODIGO = IP_FACTURADOS.CODIGO
WHERE  IP_FACTURADOS.COMPANIA = 's$compania$s'
AND    IP_FACTURADOS.CODIGO BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 
AND    IP_FACTURADOS.NUMERO_ORDEN = 's$numeroOrden$s'
AND    IP_FACTURADOS.PAGADO = 0
AND    IP_FACTURADOS.INDEXE = 0 
AND    IP_FACTURADOS.INDCAR = 0 
AND    IP_FACTURADOS.INDEXEOTROS =0
AND    IP_FACTURADOS.NOCOBRADO = 0 
AND    IP_FACTURADOS.INDPAGO_ACPAG = 0
AND IP_FACTURADOS.C1 + IP_FACTURADOS.C2 + IP_FACTURADOS.C3 + IP_FACTURADOS.C4 + IP_FACTURADOS.C13 + IP_FACTURADOS.C14 + IP_FACTURADOS.C15 + IP_FACTURADOS.C16 + IP_FACTURADOS.C17 + IP_FACTURADOS.C18 + IP_FACTURADOS.C19 + IP_FACTURADOS.C20 > 0
s$strIncluye$s 
GROUP BY IP_FACTURADOS.PREANO
HAVING IP_FACTURADOS.PREANO <= s$ano$s
