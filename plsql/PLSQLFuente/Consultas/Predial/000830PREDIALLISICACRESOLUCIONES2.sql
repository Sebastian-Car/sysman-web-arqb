WITH PREDIAL_LISICACRESOLUCIONES2 AS (
SELECT IGAC_RESOLUCIONES.RESOLUCION, 
       IGAC_RESOLUCIONES.FECHAINGRESOSISTEMA, 
       IGAC_RESOLUCIONESDET.CONSECUTIVO, 
       IGAC_RESOLUCIONESDET.MUTACION, 
       IGAC_RESOLUCIONESDET.CODIGO, 
       IGAC_RESOLUCIONESDET.CANCELAINSCRIBE, 
       IGAC_RESOLUCIONESDET.TIPOREGISTRO, 
       IGAC_RESOLUCIONESDET.NUMEROORDEN, 
       IGAC_RESOLUCIONESDET.TOTALREGISTROS, 
       IGAC_RESOLUCIONESDET.NOMBRE, 
       IGAC_RESOLUCIONESDET.ESTADOCIVIL, 
       IGAC_RESOLUCIONESDET.TIPODOCUMENTO, 
       IGAC_RESOLUCIONESDET.NUMERODOCUMENTO, 
       IGAC_RESOLUCIONESDET.DIRECCION, 
       IGAC_RESOLUCIONESDET.DESTINOECONOMICO, 
       IGAC_RESOLUCIONESDET.AREATERRENO, 
       IGAC_RESOLUCIONESDET.AREACONSTRUIDA, 
       IGAC_RESOLUCIONESDET.AVALUO, 
       IGAC_RESOLUCIONESDET.VIGENCIA, 
       IGAC_RESOLUCIONESDET.CONDICIONTRIS, 
       IGAC_RESOLUCIONESDET.ANTRESOLUCION, 
       IGAC_RESOLUCIONESDET.ANTNOMBRE, 
       IGAC_RESOLUCIONESDET.ANTTIPODOCUMENTO,
       IGAC_RESOLUCIONESDET.COMPANIA 
FROM IP_IGAC_RESOLUCIONES IGAC_RESOLUCIONES 
  INNER JOIN IP_IGAC_RESOLUCIONESDET IGAC_RESOLUCIONESDET 
  ON  IGAC_RESOLUCIONES.DEPARTAMENTO = IGAC_RESOLUCIONESDET.DEPARTAMENTO 
  AND IGAC_RESOLUCIONES.MUNICIPIO    = IGAC_RESOLUCIONESDET.MUNICIPIO 
  AND IGAC_RESOLUCIONES.RESOLUCION   = IGAC_RESOLUCIONESDET.RESOLUCION  
  AND IGAC_RESOLUCIONES.ANO          = IGAC_RESOLUCIONESDET.ANO
WHERE IGAC_RESOLUCIONES.COMPANIA   = s$compania$s
  AND IGAC_RESOLUCIONES.RESOLUCION BETWEEN 's$resolucionInicial$s' 
                                       AND 's$resolucionFinal$s'
GROUP BY IGAC_RESOLUCIONES.RESOLUCION, 
       IGAC_RESOLUCIONES.FECHAINGRESOSISTEMA, 
       IGAC_RESOLUCIONESDET.CONSECUTIVO, 
       IGAC_RESOLUCIONESDET.MUTACION, 
       IGAC_RESOLUCIONESDET.CODIGO, 
       IGAC_RESOLUCIONESDET.CANCELAINSCRIBE, 
       IGAC_RESOLUCIONESDET.TIPOREGISTRO, 
       IGAC_RESOLUCIONESDET.NUMEROORDEN, 
       IGAC_RESOLUCIONESDET.TOTALREGISTROS, 
       IGAC_RESOLUCIONESDET.NOMBRE, 
       IGAC_RESOLUCIONESDET.ESTADOCIVIL, 
       IGAC_RESOLUCIONESDET.TIPODOCUMENTO, 
       IGAC_RESOLUCIONESDET.NUMERODOCUMENTO, 
       IGAC_RESOLUCIONESDET.DIRECCION, 
       IGAC_RESOLUCIONESDET.DESTINOECONOMICO, 
       IGAC_RESOLUCIONESDET.AREATERRENO, 
       IGAC_RESOLUCIONESDET.AREACONSTRUIDA, 
       IGAC_RESOLUCIONESDET.AVALUO, 
       IGAC_RESOLUCIONESDET.VIGENCIA, 
       IGAC_RESOLUCIONESDET.CONDICIONTRIS, 
       IGAC_RESOLUCIONESDET.ANTRESOLUCION, 
       IGAC_RESOLUCIONESDET.ANTNOMBRE, 
       IGAC_RESOLUCIONESDET.COMPANIA,
       IGAC_RESOLUCIONESDET.ANTTIPODOCUMENTO
)
SELECT PREDIAL_LISICACRESOLUCIONES2.*, 
       CASE WHEN PREDIAL_LISICACRESOLUCIONES2.NOMBRE
 IS NULL 
            THEN USUARIOS_PREDIAL.NOMBRE
            ELSE PREDIAL_LISICACRESOLUCIONES2.NOMBRE
            END AS NOMBRET, 
       CASE WHEN PREDIAL_LISICACRESOLUCIONES2.TIPODOCUMENTO
  IS NULL 
            THEN USUARIOS_PREDIAL.TIPO_NIT
            ELSE PREDIAL_LISICACRESOLUCIONES2.TIPODOCUMENTO
            END AS TIPODOCT, 
       CASE WHEN PREDIAL_LISICACRESOLUCIONES2.NUMERODOCUMENTO
  IS NULL 
            THEN USUARIOS_PREDIAL.NIT
            ELSE PREDIAL_LISICACRESOLUCIONES2.NUMERODOCUMENTO
            END AS NUMERODOCT, 
       CASE WHEN PREDIAL_LISICACRESOLUCIONES2.DIRECCION
 IS NULL 
            THEN USUARIOS_PREDIAL.DIRECCION
            ELSE PREDIAL_LISICACRESOLUCIONES2.DIRECCION
            END AS DIRECCIONT, 
       CASE WHEN PREDIAL_LISICACRESOLUCIONES2.TOTALREGISTROS
 IS NULL 
            THEN USUARIOS_PREDIAL.NUMERO_REGISTROS
            ELSE PREDIAL_LISICACRESOLUCIONES2.TOTALREGISTROS
            END AS TOTALREGIST
FROM PREDIAL_LISICACRESOLUCIONES2 
  INNER JOIN IP_USUARIOS_PREDIAL USUARIOS_PREDIAL 
    ON  PREDIAL_LISICACRESOLUCIONES2.COMPANIA    = USUARIOS_PREDIAL.COMPANIA 
    and PREDIAL_LISICACRESOLUCIONES2.NUMEROORDEN = USUARIOS_PREDIAL.NUMERO_ORDEN 
    AND PREDIAL_LISICACRESOLUCIONES2.CODIGO      = USUARIOS_PREDIAL.CODIGO
