SELECT CERTIFICADO_ESTRA.CODIGO CERTIFICADO, 
       USUARIOS_PREDIAL.CODIGO PREDIO, 
       USUARIOS_PREDIAL.NOMBRE PROPIETARIO, 
       CERTIFICADO_ESTRA.IMPRESO_POR, 
       CERTIFICADO_ESTRA.FECHA_IMPRESO, 
       CERTIFICADO_ESTRA.HORA_IMPRESO, 
       CERTIFICADO_ESTRA.FORMATO
FROM IP_USUARIOS_PREDIAL USUARIOS_PREDIAL 
  INNER JOIN IP_CERTIFICADO_ESTRA 
 CERTIFICADO_ESTRA   
    ON  USUARIOS_PREDIAL.COMPANIA = CERTIFICADO_ESTRA.COMPANIA
    AND USUARIOS_PREDIAL.CODIGO   = CERTIFICADO_ESTRA.COD_PREDIO 
    AND USUARIOS_PREDIAL.NIT      = CERTIFICADO_ESTRA.PROPIETARIO
WHERE USUARIOS_PREDIAL.COMPANIA = s$compania$s
  AND CERTIFICADO_ESTRA.FORMATO BETWEEN 's$tipoCertInicial$s' 
                                    AND 's$tipoCertFinal$s' 
  AND TO_CHAR(CERTIFICADO_ESTRA.FECHA_IMPRESO, 'DD/MM/YYYY') BETWEEN s$fechaInicial$s 
  	                                      AND s$fechaFinal$s
