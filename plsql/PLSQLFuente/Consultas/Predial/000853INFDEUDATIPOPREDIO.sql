WITH CNSDEUDAXTIPOPREDIO AS 
  (
    SELECT F.CODIGO, 
           F.NUMERO_ORDEN,
           U.TIPO, 
           U.NOMBRE,
           U.NIT,
           U.DIRECCION, 
           U.AVALUO_ANO, 
           U.TRPCOD, 
           SUM(F.TOTAL) AS TOTAL, 
           SUM(F.C1) AS IMPACT, 
           0 AS IMPANT, 
           0 AS IMPDFR, 
           SUM(F.C2) AS INTIMPACT, 
           0 AS INTIMPANT,
           0 AS INTIMPDFR,
           SUM(F.C3) AS IMPCAR,
           0 AS IMPCARANT,
           0 AS IMPCARDFR, 
           SUM(F.C4) AS INTCAR,
           0 AS INTCARANT, 
           0 AS INTCARDFR, 
           SUM(F.C9+F.C10+F.C11+F.C12) AS ACUMDIFERIDO, 
           SUM(F.C13) AS DESCUENTO, 
           SUM(F.C14+F.C15+F.C16+F.C17+F.C18+F.C19+F.C20) AS OTROS, 
           TP.NOMBRE AS TIPONOM
    FROM IP_FACTURADOS F INNER JOIN IP_USUARIOS_PREDIAL U 
           ON F.COMPANIA     = U.COMPANIA
          AND F.CODIGO       = U.CODIGO 
          AND F.NUMERO_ORDEN = U.NUMERO_ORDEN
        INNER JOIN IP_TIPO_PREDIO TP
           ON U.TIPO = TP.CODIGO 
    WHERE F.COMPANIA       = s$compania$s
      AND F.NUMERO_ORDEN     = 's$numeroOrden$s'
      AND F.PREANO         = EXTRACT(YEAR FROM (SYSDATE))
    AND NVL(F.PAGADO,0)    IN (0) 
    AND NVL(F.NOCOBRADO,0) IN (0)
    AND NVL(F.INDPAGO_ACPAG,0) IN (0) 
    AND U.TIPO BETWEEN 's$tipoInicial$s' AND 's$tipoFinal$s'
    AND U.INDEXE           IN (0)
    AND U.INDCAR           IN (0)
    AND U.INDBORRADO       IN (0)
    AND U.CODIGO_NO_ACTIVO IN (0)
    AND F.INDCAR           IN (0)
    AND F.INDEXE           IN (0)
    GROUP BY F.CODIGO, F.NUMERO_ORDEN, U.TIPO, U.NOMBRE, U.NIT, U.DIRECCION, U.AVALUO_ANO, U.TRPCOD, TP.NOMBRE, F.PAGADO
    UNION
    SELECT F.CODIGO, 
           F.NUMERO_ORDEN,
           U.TIPO, 
           U.NOMBRE,
           U.NIT, 
           U.DIRECCION, 
           U.AVALUO_ANO, 
           U.TRPCOD, 
           SUM(F.TOTAL) AS TOTAL, 
           0 AS IMPACT, 
           SUM(F.C1) AS IMPANT, 
           0 AS IMPDFR, 
           0 AS INTIMPACT, 
           SUM(F.C2) AS INTIMPANT,  
           0 AS INTIMPDFR, 
           0 AS IMPCARACT, 
           SUM(F.C3) AS IMPCARANT, 
           0 AS IMPCARDFR, 
           0 AS INTCARACT, 
           SUM(F.C4) AS INTCARANT,  
           0 AS INTCARDFR, 
           SUM(F.C9+F.C10+F.C11+F.C12) AS ACUMDIFERIDO, 
           SUM(F.C13) AS DESCUENTO, 
           SUM(F.C14+F.C15+F.C16+F.C17+F.C18+F.C19+F.C20) AS OTROS, 
           TP.NOMBRE AS TIPONOM
    FROM IP_FACTURADOS F INNER JOIN IP_USUARIOS_PREDIAL U 
           ON F.COMPANIA     = U.COMPANIA
          AND F.CODIGO       = U.CODIGO 
          AND F.NUMERO_ORDEN = U.NUMERO_ORDEN
        INNER JOIN IP_TIPO_PREDIO TP
           ON U.TIPO = TP.CODIGO
    WHERE F.COMPANIA 		 = s$compania$s
      AND F.NUMERO_ORDEN 	 = 's$numeroOrden$s'
      AND F.PREANO 			 = EXTRACT(YEAR FROM (SYSDATE)) -1
      AND NVL(F.PAGADO,0)    IN (0) 
      AND NVL(F.NOCOBRADO,0) IN (0)
      AND NVL(F.INDPAGO_ACPAG,0) IN (0) 
      AND U.TIPO BETWEEN 's$tipoInicial$s' AND 's$tipoFinal$s'
      AND U.INDEXE           IN (0)
      AND U.INDCAR           IN (0)
      AND U.INDBORRADO       IN (0)
      AND U.CODIGO_NO_ACTIVO IN (0)
      AND F.INDCAR           IN (0)
      AND F.INDEXE           IN (0)
    GROUP BY F.CODIGO, F.NUMERO_ORDEN, U.TIPO, U.NOMBRE, U.NIT, U.DIRECCION, U.AVALUO_ANO, U.TRPCOD, TP.NOMBRE, F.PAGADO
    UNION 
    SELECT F.CODIGO, 
           F.NUMERO_ORDEN, 
           U.TIPO,
           U.NOMBRE,
           U.NIT,
           U.DIRECCION, 
           U.AVALUO_ANO,
           U.TRPCOD, 
           SUM(F.TOTAL) AS TOTAL, 
           0 AS IMPACT,
           0 AS IMPANT, 
           SUM(F.C1) AS IMPDFR, 
           0 AS INTIMPACT, 
           0 AS INTIMPANT, 
           SUM(F.C2) AS INTIMPDFR,
           0 AS IMPCARACT, 
           0 AS IMPCARANT,  
           SUM(F.C3) AS IMPCARDFR, 
           0 AS INTCARACT, 
           0 AS INTCARANT, 
           SUM(F.C4) AS INTCARDFR,
           SUM(F.C9+F.C10+F.C11+F.C12) AS ACUMDIFERIDO,
           SUM(F.C13) AS DESCUENTO, 
           SUM(F.C14+F.C15+F.C16+F.C17+F.C18+F.C19+F.C20) AS OTROS, 
           TP.NOMBRE AS TIPONOM
    FROM IP_FACTURADOS F INNER JOIN IP_USUARIOS_PREDIAL U 
           ON F.COMPANIA     = U.COMPANIA
          AND F.CODIGO       = U.CODIGO 
          AND F.NUMERO_ORDEN = U.NUMERO_ORDEN
        INNER JOIN IP_TIPO_PREDIO TP
           ON U.TIPO = TP.CODIGO       
    WHERE F.COMPANIA 		 = s$compania$s
      AND F.NUMERO_ORDEN 	 = 's$numeroOrden$s'
      AND F.PREANO 			 < EXTRACT(YEAR FROM (SYSDATE)) -1
      AND NVL(F.PAGADO,0)    IN (0) 
      AND NVL(F.NOCOBRADO,0) IN (0)
      AND NVL(F.INDPAGO_ACPAG,0) IN (0) 
      AND U.TIPO BETWEEN 's$tipoInicial$s' AND 's$tipoFinal$s'
      AND U.INDEXE           IN (0)
      AND U.INDCAR           IN (0)
      AND U.INDBORRADO       IN (0) 
      AND U.CODIGO_NO_ACTIVO IN (0) 
      AND F.INDCAR           IN (0)
      AND F.INDEXE           IN (0)
    GROUP BY F.CODIGO, F.NUMERO_ORDEN, U.TIPO, U.NOMBRE, U.NIT, U.DIRECCION, U.AVALUO_ANO, U.TRPCOD, TP.NOMBRE, F.PAGADO 
  )
  SELECT CNSDEUDAXTIPOPREDIO.CODIGO,
       CNSDEUDAXTIPOPREDIO.NUMERO_ORDEN,
       CNSDEUDAXTIPOPREDIO.TIPO, 
       CNSDEUDAXTIPOPREDIO.NOMBRE,
       CNSDEUDAXTIPOPREDIO.NIT, 
       CNSDEUDAXTIPOPREDIO.DIRECCION, 
       CNSDEUDAXTIPOPREDIO.AVALUO_ANO,
       CNSDEUDAXTIPOPREDIO.TRPCOD,
       SUM(CNSDEUDAXTIPOPREDIO.TOTAL) AS SUMADETOTAL, 
       SUM(CNSDEUDAXTIPOPREDIO.IMPACT) AS SUMADEIMPACT, 
       SUM(CNSDEUDAXTIPOPREDIO.IMPANT) AS SUMADEIMPANT, 
       SUM(CNSDEUDAXTIPOPREDIO.IMPDFR) AS SUMADEIMPDFR,
       SUM(CNSDEUDAXTIPOPREDIO.INTIMPACT) AS SUMADEINTIMPACT, 
       SUM(CNSDEUDAXTIPOPREDIO.INTIMPANT) AS SUMADEINTIMPANT,
       SUM(CNSDEUDAXTIPOPREDIO.INTIMPDFR) AS SUMADEINTIMPDFR, 
       SUM(CNSDEUDAXTIPOPREDIO.IMPCAR) AS SUMADEIMPCAR,
       SUM(CNSDEUDAXTIPOPREDIO.IMPCARANT) AS SUMADEIMPCARANT, 
       SUM(CNSDEUDAXTIPOPREDIO.IMPCARDFR) AS SUMADEIMPCARDFR,
       SUM(CNSDEUDAXTIPOPREDIO.INTCAR) AS SUMADEINTCAR, 
       SUM(CNSDEUDAXTIPOPREDIO.INTCARANT) AS SUMADEINTCARANT, 
       SUM(CNSDEUDAXTIPOPREDIO.INTCARDFR) AS SUMADEINTCARDFR,
       SUM(CNSDEUDAXTIPOPREDIO.ACUMDIFERIDO) AS SUMADEACUMDIFERIDO, 
       SUM(CNSDEUDAXTIPOPREDIO.DESCUENTO) AS SUMADEDESCUENTO, 
       SUM(CNSDEUDAXTIPOPREDIO.OTROS) AS SUMADEOTROS, 
       CNSDEUDAXTIPOPREDIO.TIPONOM
FROM CNSDEUDAXTIPOPREDIO
GROUP BY CNSDEUDAXTIPOPREDIO.CODIGO, CNSDEUDAXTIPOPREDIO.NUMERO_ORDEN, CNSDEUDAXTIPOPREDIO.TIPO, 
         CNSDEUDAXTIPOPREDIO.NOMBRE, CNSDEUDAXTIPOPREDIO.NIT, CNSDEUDAXTIPOPREDIO.DIRECCION, 
         CNSDEUDAXTIPOPREDIO.AVALUO_ANO, CNSDEUDAXTIPOPREDIO.TRPCOD, CNSDEUDAXTIPOPREDIO.TIPONOM
