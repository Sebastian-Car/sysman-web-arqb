INNER JOIN IP_USUARIOS_PREDIAL   
ON  IP_RECIBOS_DE_PAGO.COMPANIA      = IP_USUARIOS_PREDIAL.COMPANIA 
AND IP_RECIBOS_DE_PAGO.PRECOD        = IP_USUARIOS_PREDIAL.CODIGO 
AND IP_RECIBOS_DE_PAGO.NUMERO_ORDEN  = IP_USUARIOS_PREDIAL.NUMERO_ORDEN 
INNER JOIN IP_BANCOS  
ON   IP_RECIBOS_DE_PAGO.COMPANIA     = IP_BANCOS.COMPANIA 
AND  IP_RECIBOS_DE_PAGO.PAG_BANPAG   = IP_BANCOS.CODIGOBANCO 
WHERE  IP_RECIBOS_DE_PAGO.COMPANIA = s$compania$s 
AND    IP_RECIBOS_DE_PAGO.PAG_BANPAG BETWEEN 's$codigoBancoInicial$s' AND 's$codigoBancoFinal$s' 
AND    IP_USUARIOS_PREDIAL.NUMERO_ORDEN = 's$numeroOrden$s'  
AND    IP_RECIBOS_DE_PAGO.PREFECPAG BETWEEN s$fechaInicial$s AND  s$fechaFinal$s  
AND    IP_RECIBOS_DE_PAGO.PAQUETEPAG BETWEEN 's$cmbPaqIni$s' AND  's$cmbPaqFin$s'  
AND    IP_RECIBOS_DE_PAGO.ANULADO = 0  
AND    IP_RECIBOS_DE_PAGO.PAGO NOT IN (0) 
ORDER BY IP_RECIBOS_DE_PAGO.DOCNUM
