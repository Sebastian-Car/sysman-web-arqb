SELECT IP_USUARIOS_PREDIAL.CODIGO, 
       IP_USUARIOS_PREDIAL.NIT, 
       IP_USUARIOS_PREDIAL.NOMBRE, 
       IP_USUARIOS_PREDIAL.PAGO_ANO, 
       IP_USUARIOS_PREDIAL.DIRECCION, 
       IP_USUARIOS_PREDIAL.PAG_FEC, 
       SUM(IP_FACTURADOS.TOTAL)  DEUDA, 
       IP_USUARIOS_PREDIAL.M_INMOBILIARIA
FROM   IP_USUARIOS_PREDIAL 
INNER JOIN IP_FACTURADOS  
ON     IP_USUARIOS_PREDIAL.COMPANIA = IP_FACTURADOS.COMPANIA
AND    IP_USUARIOS_PREDIAL.CODIGO = IP_FACTURADOS.CODIGO
AND    IP_USUARIOS_PREDIAL.NUMERO_ORDEN = IP_FACTURADOS.NUMERO_ORDEN 
WHERE  IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s
AND    IP_USUARIOS_PREDIAL.CODIGO BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
AND    IP_USUARIOS_PREDIAL.NUMERO_ORDEN = 's$numeroOrden$s'
AND    NVL(IP_USUARIOS_PREDIAL.INDBORRADO, 0) = 0
AND    NVL(IP_USUARIOS_PREDIAL.CODIGO_NO_ACTIVO, 0) = 0 
AND    NVL(IP_USUARIOS_PREDIAL.INDEXE, 0) = 0
AND    NVL(IP_USUARIOS_PREDIAL.INDCAR, 0) = 0
AND    NVL(IP_FACTURADOS.INDEXE, 0) = 0 
AND    NVL(IP_FACTURADOS.INDCAR, 0) = 0 
AND    NVL(IP_FACTURADOS.PAGADO, 0) = 0 
AND    NVL(IP_FACTURADOS.NOCOBRADO, 0) = 0
s$condicion$s
AND    IP_USUARIOS_PREDIAL.PAG_FEC IS NULL 
GROUP BY 
    IP_USUARIOS_PREDIAL.CODIGO, 
    IP_USUARIOS_PREDIAL.NIT, 
    IP_USUARIOS_PREDIAL.NOMBRE, 
    IP_USUARIOS_PREDIAL.PAGO_ANO, 
    IP_USUARIOS_PREDIAL.DIRECCION, 
    IP_USUARIOS_PREDIAL.PAG_FEC, 
    IP_USUARIOS_PREDIAL.M_INMOBILIARIA
HAVING SUM(IP_FACTURADOS.TOTAL) > 0
