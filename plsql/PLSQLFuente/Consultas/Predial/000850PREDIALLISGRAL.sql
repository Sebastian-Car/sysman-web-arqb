SELECT  IP_USUARIOS_PREDIAL.CODIGO,
        IP_USUARIOS_PREDIAL.NOMBRE,
        IP_USUARIOS_PREDIAL.DIRECCION,
        IP_USUARIOS_PREDIAL.AVALUO_ANO,
        IP_USUARIOS_PREDIAL.PAGO_ANO,
        IP_USUARIOS_PREDIAL.M_INMOBILIARIA
FROM IP_USUARIOS_PREDIAL 
WHERE IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s 
     AND IP_USUARIOS_PREDIAL.CODIGO BETWEEN s$codigoInicial$s AND s$codigoFinal$s
     AND IP_USUARIOS_PREDIAL.AVALUO_ANO BETWEEN s$avaluoInicial$s AND s$avaluoFinal$s
     s$condicion$s
