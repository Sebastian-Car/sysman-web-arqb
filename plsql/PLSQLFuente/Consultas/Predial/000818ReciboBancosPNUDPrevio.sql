SELECT  IP_FACTURADOS.COMPANIA,
        IP_FACTURADOS.CODIGO CODIGO,
        IP_FACTURADOS.NUMERO_ORDEN,
        IP_FACTURADOS.PREANO,
        MAX(IP_FACTURADOS.TRPPOR) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) TRPPOR,
        MAX(IP_FACTURADOS.AVALUO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AVALUO,
        SUM(IP_FACTURADOS.C1)      C1,
        SUM(IP_FACTURADOS.C3)      C3,
        SUM(IP_FACTURADOS.C4)      C4,
        SUM(IP_FACTURADOS.C2)      C2,
        SUM(IP_FACTURADOS.C6)      C6,
        SUM(IP_FACTURADOS.C8)      C8,
        SUM(IP_FACTURADOS.C10)     C10,
        SUM(IP_FACTURADOS.C12)     C12,
        SUM(IP_FACTURADOS.C5)      C5,
        SUM(IP_FACTURADOS.C17)     C7,
        SUM(IP_FACTURADOS.C9)      C9,
        SUM(IP_FACTURADOS.C11)     C11,
        SUM(IP_FACTURADOS.C15)     C15,
        SUM(IP_FACTURADOS.C16)     C16,
        SUM(IP_FACTURADOS.C17)     C17,
        SUM(IP_FACTURADOS.C18)     C18,
        SUM(IP_FACTURADOS.C19)     C19,
        SUM(IP_FACTURADOS.C20)     C20,
        SUM(IP_FACTURADOS.C13)     C13,
        SUM(IP_FACTURADOS.C14)     C14,
        SUM(IP_FACTURADOS.TOTAL)   TOTAL,
        0                          DESCAPARTE
FROM    IP_FACTURADOS
WHERE   IP_FACTURADOS.COMPANIA     = $P{PR_COMPANIA}
  AND   IP_FACTURADOS.CODIGO       = $P{PR_CODIGO}
  AND   IP_FACTURADOS.NUMERO_ORDEN = $P{PR_NUMERO_ORDEN}
  AND   IP_FACTURADOS.PAGADO       IN (0)
  AND   IP_FACTURADOS.NOCOBRADO    IN (0)
  s$condicionSub$s
GROUP BY IP_FACTURADOS.PREANO,
        0,
        IP_FACTURADOS.COMPANIA,
        IP_FACTURADOS.CODIGO,
        IP_FACTURADOS.NUMERO_ORDEN
