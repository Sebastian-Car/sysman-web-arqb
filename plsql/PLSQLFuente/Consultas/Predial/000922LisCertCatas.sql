SELECT NUMCER,
  CODIGOPREDIO,
  NOMBRE,
  VALOR,
  CASE ANULADO WHEN 0 THEN ' ' ELSE 'X' END ANULADO,
  (VALOR) AS VLR
FROM IP_CERTIFICADOCATAS
WHERE 
  COMPANIA = s$compania$s
  AND (FECHA_EXP
  BETWEEN s$fechaInicial$s AND s$fechaFinal$s
OR FECHA_ANULADO
  BETWEEN s$fechaInicial$s AND s$fechaFinal$s)
