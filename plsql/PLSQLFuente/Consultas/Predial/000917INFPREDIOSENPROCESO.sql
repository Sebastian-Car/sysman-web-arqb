SELECT  IP_USUARIOS_PREDIAL.CODIGO, 
        IP_USUARIOS_PREDIAL.NOMBRE, 
        IP_USUARIOS_PREDIAL.NIT, 
        IP_USUARIOS_PREDIAL.DIRECCION,
        IP_USUARIOS_PREDIAL.PAGO_ANO, 
        IP_USUARIOS_PREDIAL.NUMERO_PROCESO, 
        IP_AUDITORIA.MODOPE AS USUARIO, 
        IP_AUDITORIA.MODFEC
FROM IP_USUARIOS_PREDIAL 
  INNER JOIN IP_AUDITORIA 
    ON IP_USUARIOS_PREDIAL.COMPANIA = IP_AUDITORIA.COMPANIA
    AND IP_USUARIOS_PREDIAL.CODIGO = IP_AUDITORIA.MODCOD
WHERE IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s
  AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN = s$numeroOrden$s
  AND IP_AUDITORIA.MODFEC BETWEEN s$fechaInicial$s AND s$fechaFinal$s 
  AND IP_AUDITORIA.MODCCO = '114' 
  AND IP_USUARIOS_PREDIAL.PROCESO_DE_COBRO NOT IN(0)
