SELECT  IP_ACUERDOS.PREDIO, 
        IP_USUARIOS_PREDIAL.NOMBRE, 
        IP_USUARIOS_PREDIAL.TIPO_NIT, 
        IP_USUARIOS_PREDIAL.NIT, 
        IP_ACUERDOS.CODIGOACUERDO, 
        IP_ACUERDOS.ID_RESP, 
        IP_ACUERDOS.NOMBRE_RESP, 
        IP_ACUERDOS.MONTOCAPITAL, 
        IP_ACUERDOS.MONTOINTERESES, 
        IP_ACUERDOS.NCUOTAS, 
        IP_ACUERDOS.FECHAANULADO, 
        IP_ACUERDOS.ELIMINADO_POR
FROM IP_ACUERDOS  
  INNER JOIN IP_USUARIOS_PREDIAL 
    ON IP_ACUERDOS.COMPANIA 		= IP_USUARIOS_PREDIAL.COMPANIA
    AND IP_ACUERDOS.PREDIO 		= IP_USUARIOS_PREDIAL.CODIGO
    AND IP_ACUERDOS.NUMERO_ORDEN 	= IP_USUARIOS_PREDIAL.NUMERO_ORDEN
WHERE IP_ACUERDOS.COMPANIA = s$compania$s
  AND IP_ACUERDOS.CODIGOACUERDO BETWEEN s$acuerdoInicial$s AND s$acuerdoFinal$s 
  AND TRUNC(IP_ACUERDOS.FECHAANULADO) BETWEEN s$fechaInicial$s AND s$fechaFinal$s 
  AND IP_ACUERDOS.ANULADO NOT IN(0) 
ORDER BY IP_ACUERDOS.FECHAANULADO
