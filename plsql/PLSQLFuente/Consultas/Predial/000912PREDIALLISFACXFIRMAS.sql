SELECT  IP_USUARIOS_PREDIAL.CODIGO,
        IP_USUARIOS_PREDIAL.NUMERO_ORDEN,
        IP_RECIBOS_DE_PAGO.DOCNUM,
        IP_USUARIOS_PREDIAL.NOMBRE,
        IP_USUARIOS_PREDIAL.DIRECCION,
        IP_RECIBOS_DE_PAGO.PREVAL,
        IP_RECIBOS_DE_PAGO.ANULADO,
        IP_RECIBOS_DE_PAGO.PAGO
FROM IP_USUARIOS_PREDIAL
  INNER JOIN IP_RECIBOS_DE_PAGO
    ON IP_USUARIOS_PREDIAL.COMPANIA       = IP_RECIBOS_DE_PAGO.COMPANIA
    AND IP_USUARIOS_PREDIAL.CODIGO        = IP_RECIBOS_DE_PAGO.PRECOD
    AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN  = IP_RECIBOS_DE_PAGO.NUMERO_ORDEN
WHERE IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s
  AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN    = s$numeroOrden$s
  AND IP_RECIBOS_DE_PAGO.DOCNUM BETWEEN s$facturaInicial$s AND s$facturaFinal$s
  AND IP_RECIBOS_DE_PAGO.PREVAL   > 0
  AND IP_RECIBOS_DE_PAGO.ANULADO  IN(0)
  AND IP_RECIBOS_DE_PAGO.PAGO     IN(0)
ORDER BY IP_RECIBOS_DE_PAGO.DOCNUM
