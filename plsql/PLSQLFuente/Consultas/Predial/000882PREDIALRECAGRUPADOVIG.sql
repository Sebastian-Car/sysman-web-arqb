SELECT  IP_FACTURADOS.PREANO AS ANO, 
        SUM(IP_FACTURADOS.C1) AS SUMAC1, 
        SUM(IP_FACTURADOS.C2) AS SUMAC2, 
        SUM(IP_FACTURADOS.C3) AS SUMAC3, 
        SUM(IP_FACTURADOS.C4) AS SUMAC4
FROM IP_FACTURADOS
WHERE IP_FACTURADOS.COMPANIA = s$compania$s
  AND IP_FACTURADOS.FECHAPAGO BETWEEN s$fechaInicial$s AND s$fechaFinal$s
GROUP BY IP_FACTURADOS.PREANO
