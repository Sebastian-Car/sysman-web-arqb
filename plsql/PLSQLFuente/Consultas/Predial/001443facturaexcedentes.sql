WITH AGRUPADO AS (
SELECT 
     IP_RECIBO_EXCEDENTES.COMPANIA
    ,IP_RECIBO_EXCEDENTES.NUMERO_FACTURA
    ,IP_RECIBO_EXCEDENTES.CODIGO_PREDIO
    ,IP_RECIBO_EXCEDENTES.NUMERO_ORDEN
    ,SUM(IP_RECIBO_EXCEDENTES.VALOR) S_VALOR
FROM IP_RECIBO_EXCEDENTES  
WHERE IP_RECIBO_EXCEDENTES.COMPANIA            = s$compania$s  
  AND IP_RECIBO_EXCEDENTES.CODIGO_PREDIO     = 's$predio$s' 
  AND IP_RECIBO_EXCEDENTES.NUMERO_ORDEN     = 's$numeroOrden$s' 
  AND IP_RECIBO_EXCEDENTES.NUMERO_FACTURA = 's$numeroFactura$s'  
  AND IP_RECIBO_EXCEDENTES.ANULADO       IN (0)
  AND IP_RECIBO_EXCEDENTES.PAGO              IN (0) 
GROUP BY 
   IP_RECIBO_EXCEDENTES.COMPANIA
  ,IP_RECIBO_EXCEDENTES.NUMERO_FACTURA
  ,IP_RECIBO_EXCEDENTES.CODIGO_PREDIO
  ,IP_RECIBO_EXCEDENTES.NUMERO_ORDEN 
),
COPROPIETARIOS AS (
SELECT 
   COMPANIA 
  ,CODIGO 
  ,LISTAGG(TRIM(IP_USUARIOS_PREDIAL.NOMBRE), ', ') 
   WITHIN GROUP (ORDER BY IP_USUARIOS_PREDIAL.NUMERO_ORDEN) NOMBRES
 FROM IP_USUARIOS_PREDIAL
WHERE COMPANIA         = s$compania$s
  AND NUMERO_ORDEN     NOT IN ('s$numeroOrden$s')
  AND CODIGO           = 's$predio$s' 
GROUP BY   COMPANIA
         , CODIGO
)
SELECT 
   IP_RECIBO_EXCEDENTES.CODIGO_PREDIO
  ,IP_RECIBO_EXCEDENTES.NOMBRE
  ,IP_RECIBO_EXCEDENTES.FECHA_EXPEDICION
  ,IP_RECIBO_EXCEDENTES.NUMERO_FACTURA
  ,TO_CHAR(PCK_CODIGODEBARRAS.FC_IMPRIMIRCODIGODEBARRAS(
                              UN_TEXTOPARACONVERTIR => CHR(205)||CHR(102)||'415'||'s$codigoEAN$s'
                                                      ||'8020'||SUBSTR(IP_RECIBO_EXCEDENTES.CODIGO_PREDIO,2,14)
                                                      ||SUBSTR(IP_RECIBO_EXCEDENTES.NUMERO_FACTURA,3,8)||'01'
                                                      ||'3900'||LPAD('s$valorFactura$s',14,'0')||'96'
                                                      ||TO_CHAR(IP_RECIBO_EXCEDENTES.FECHA_EXPEDICION,'YYYYMMDD'))) CODIGODEBARRAS_BIN
  ,CHR(40) || '415' || CHR(41) || 's$codigoEAN$s' 
  || CHR(40) || '8020' || CHR(41) 
  || SUBSTR(IP_RECIBO_EXCEDENTES.CODIGO_PREDIO,2,14)||SUBSTR(IP_RECIBO_EXCEDENTES.NUMERO_FACTURA,3,8)
  ||'01'|| CHR(40) || '3900' || CHR(41) 
  || LPAD('s$valorFactura$s',14,'0') || CHR(40) || '96' || CHR(41) 
  || TO_CHAR(IP_RECIBO_EXCEDENTES.FECHA_EXPEDICION,'YYYYMMDD') BARRAS
  ,IP_USUARIOS_PREDIAL.AREA_HA
  ,IP_USUARIOS_PREDIAL.AREA_M2
  ,IP_USUARIOS_PREDIAL.AREA_CONSTRUIDA 
  ,IP_RECIBOS_DE_PAGO.PREANOI||' - '||IP_RECIBOS_DE_PAGO.PREANOF VIGENCIAS
  ,IP_RECIBOS_DE_PAGO.PREFECLIM
  ,IP_USUARIOS_PREDIAL.DESTINO_ECONOMICO
  ,COPROPIETARIOS.NOMBRES COPROPIETARIOS
  ,AGRUPADO.S_VALOR S_TOTAL
  ,IP_USUARIOS_PREDIAL.NIT
  ,IP_USUARIOS_PREDIAL.COD_POSTAL
  ,IP_USUARIOS_PREDIAL.DIRECCION 

    
FROM IP_RECIBO_EXCEDENTES 
    INNER JOIN IP_RECIBOS_DE_PAGO 
        ON  IP_RECIBO_EXCEDENTES.NUMERO_FACTURA = IP_RECIBOS_DE_PAGO.DOCNUM 
        AND IP_RECIBO_EXCEDENTES.CODIGO_PREDIO  = IP_RECIBOS_DE_PAGO.PRECOD
        AND IP_RECIBO_EXCEDENTES.NUMERO_ORDEN   = IP_RECIBOS_DE_PAGO.NUMERO_ORDEN
    LEFT JOIN IP_USUARIOS_PREDIAL 
        ON  IP_RECIBO_EXCEDENTES.COMPANIA      = IP_USUARIOS_PREDIAL.COMPANIA
        AND IP_RECIBO_EXCEDENTES.CODIGO_PREDIO = IP_USUARIOS_PREDIAL.CODIGO
        AND IP_RECIBO_EXCEDENTES.NUMERO_ORDEN  = IP_USUARIOS_PREDIAL.NUMERO_ORDEN
    LEFT JOIN AGRUPADO 
        ON  IP_RECIBO_EXCEDENTES.COMPANIA       = AGRUPADO.COMPANIA
        AND IP_RECIBO_EXCEDENTES.CODIGO_PREDIO  = AGRUPADO.CODIGO_PREDIO
        AND IP_RECIBO_EXCEDENTES.NUMERO_FACTURA = AGRUPADO.NUMERO_FACTURA
        AND IP_RECIBO_EXCEDENTES.NUMERO_ORDEN   = AGRUPADO.NUMERO_ORDEN
    LEFT JOIN COPROPIETARIOS
        ON  IP_RECIBO_EXCEDENTES.COMPANIA             = COPROPIETARIOS.COMPANIA
        AND IP_RECIBO_EXCEDENTES.CODIGO_PREDIO  = COPROPIETARIOS.CODIGO
WHERE IP_RECIBO_EXCEDENTES.COMPANIA       = s$compania$s
  AND IP_RECIBO_EXCEDENTES.CODIGO_PREDIO  = 's$predio$s'
  AND IP_RECIBO_EXCEDENTES.NUMERO_ORDEN   = 's$numeroOrden$s'
  AND IP_RECIBO_EXCEDENTES.NUMERO_FACTURA = 's$numeroFactura$s' 
  AND IP_RECIBO_EXCEDENTES.ANULADO        IN (0) 
  AND IP_RECIBO_EXCEDENTES.PAGO           IN (0)
