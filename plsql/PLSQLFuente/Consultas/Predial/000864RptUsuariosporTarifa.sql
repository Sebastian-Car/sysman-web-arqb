SELECT  CASE IP_USUARIOS_PREDIAL.INDEXE 
             WHEN 0 THEN 'Predios sin Exentos' 
                    ELSE 'Predios Exentos' END INDEXE,
        IP_USUARIOS_PREDIAL.M_INMOBILIARIA,
        s$trpCod$s,
        IP_TARIFAS.TRPDES NOMBRETARIFA,
        IP_TARIFAS.TRPPOR AS PORCENTAJE,
        IP_USUARIOS_PREDIAL.CODIGO,
        IP_USUARIOS_PREDIAL.NOMBRE,
        s$avaluoAno$s
FROM IP_USUARIOS_PREDIAL
  INNER JOIN IP_FACTURADOS
    ON IP_USUARIOS_PREDIAL.COMPANIA       = IP_FACTURADOS.COMPANIA
    AND IP_USUARIOS_PREDIAL.CODIGO        = IP_FACTURADOS.CODIGO
    AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN  = IP_FACTURADOS.NUMERO_ORDEN
  INNER JOIN IP_TARIFAS
    ON IP_FACTURADOS.COMPANIA = IP_TARIFAS.COMPANIA
    AND IP_FACTURADOS.PREANO  = IP_TARIFAS.TRPANO
    AND IP_FACTURADOS.TRPCOD  = IP_TARIFAS.TRPCOD
    AND IP_FACTURADOS.TRPRAN  = IP_TARIFAS.TRPRAN
WHERE IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s
   s$like$s
  AND IP_TARIFAS.TRPANO = s$ano$s
  AND IP_TARIFAS.TRPCOD BETWEEN s$tarifaInicial$s AND s$tarifaFinal$s
  AND IP_USUARIOS_PREDIAL.INDBORRADO       IN (0)
  AND IP_USUARIOS_PREDIAL.CODIGO_NO_ACTIVO IN (0)      
ORDER BY IP_USUARIOS_PREDIAL.CODIGO
