SELECT IP_PAGOSDOBLES.PRECOD, 
IP_USUARIOS_PREDIAL.NOMBRE, 
IP_PAGOSDOBLES.PREANO, 
TO_CHAR(IP_PAGOSDOBLES.FECHA,'DD/MM/YYYY') FECHA,
IP_PAGOSDOBLES.CREATED_BY, 
DECODE(IP_PAGOSDOBLES.TIPO,'S','SALDO','D','EXCEDENTE') AS TIPO,
IP_PAGOSDOBLES.VALOR AS TXTVALOR, 
IP_USUARIOS_PREDIAL.NUMERO_ORDEN 
FROM IP_PAGOSDOBLES 
LEFT JOIN IP_USUARIOS_PREDIAL ON 
IP_PAGOSDOBLES.COMPANIA = IP_USUARIOS_PREDIAL.COMPANIA AND
IP_PAGOSDOBLES.PRECOD = IP_USUARIOS_PREDIAL.CODIGO AND
IP_PAGOSDOBLES.NUMERO_ORDEN = IP_USUARIOS_PREDIAL.NUMERO_ORDEN
WHERE IP_PAGOSDOBLES.COMPANIA = s$compania$s
AND IP_PAGOSDOBLES.FECHA BETWEEN 
      s$fechaInicial$s AND s$fechaFinal$s
AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN='s$numeroOrden$s'
