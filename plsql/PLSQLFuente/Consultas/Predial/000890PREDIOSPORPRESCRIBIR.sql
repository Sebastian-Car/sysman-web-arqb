SELECT CODIGO,
       NOMBRE, 
       DIRECCION,
       AVALUO_ANO,
       PAGO_ANO, 
       PCK_SYSMAN_UTL.FC_PAR(s$compania$s,'AÑO MAXIMO DE PRESCRIPCION', PCK_DATOS.FC_MODULOPREDIAL, SYSDATE) AS PRESCRIBEHASTA
FROM IP_USUARIOS_PREDIAL 
WHERE COMPANIA         = s$compania$s
  AND NUMERO_ORDEN     = '001'
  AND INDBORRADO       IN (0) 
  AND CODIGO_NO_ACTIVO IN (0) 
  AND ACUERDO_PAGO     IN (0)
  AND PAGO_ANO < PCK_SYSMAN_UTL.FC_PAR(s$compania$s,'AÑO MAXIMO DE PRESCRIPCION', PCK_DATOS.FC_MODULOPREDIAL, SYSDATE)
GROUP BY CODIGO, NOMBRE, DIRECCION, AVALUO_ANO, PAGO_ANO
