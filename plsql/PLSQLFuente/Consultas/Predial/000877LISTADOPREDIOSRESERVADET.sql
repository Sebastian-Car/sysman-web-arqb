SELECT  IP_RECIBOS_DE_PAGO.PREFECPAG,
        IP_BANCOS.NOMBREBANCO,
        IP_FACTURADOS.DOCNUM,
        IP_FACTURADOS.CODIGO,
        IP_FACTURADOS.PREANO,
        IP_USUARIOS_PREDIAL.NOMBRE,
        IP_USUARIOS_PREDIAL.PORCENTAJE_RESERVA, 
        NVL(IP_FACTURADOS.C1,0) C1,
        NVL(IP_FACTURADOS.C2,0) C2,
        NVL(IP_FACTURADOS.C14,0) + NVL(IP_FACTURADOS.C17,0) + 
        NVL(IP_FACTURADOS.C18,0) + NVL(IP_FACTURADOS.C20,0) OTROS,  
        NVL(IP_FACTURADOS.C3,0) C3,
        NVL(IP_FACTURADOS.C4,0) C4,
        NVL(IP_FACTURADOS.C1,0) + NVL(IP_FACTURADOS.C2,0) + NVL(IP_FACTURADOS.C3,0) + 
        NVL(IP_FACTURADOS.C4,0) + NVL(IP_FACTURADOS.C5,0) + NVL(IP_FACTURADOS.C6,0) +
        NVL(IP_FACTURADOS.C7,0) + NVL(IP_FACTURADOS.C8,0) + NVL(IP_FACTURADOS.C9,0) +
        NVL(IP_FACTURADOS.C10,0)+ NVL(IP_FACTURADOS.C11,0)+ NVL(IP_FACTURADOS.C12,0)+
        NVL(IP_FACTURADOS.C13,0)+ NVL(IP_FACTURADOS.C14,0)+ NVL(IP_FACTURADOS.C15,0)+
        NVL(IP_FACTURADOS.C16,0)+ NVL(IP_FACTURADOS.C17,0)+ NVL(IP_FACTURADOS.C18,0)+
        NVL(IP_FACTURADOS.C19,0)+ NVL(IP_FACTURADOS.C20,0)+ 
        NVL(IP_RECIBOS_DE_PAGO.INTERES_ACUERDO,0) + NVL(IP_RECIBOS_DE_PAGO.INTERES_RECARGO,0) TOTAL 
FROM IP_FACTURADOS 
LEFT JOIN IP_RECIBOS_DE_PAGO
ON  IP_RECIBOS_DE_PAGO.COMPANIA = IP_FACTURADOS.COMPANIA
AND IP_RECIBOS_DE_PAGO.PRECOD   = IP_FACTURADOS.CODIGO 
AND IP_RECIBOS_DE_PAGO.PREANO   = IP_FACTURADOS.PREANO
AND IP_RECIBOS_DE_PAGO.DOCNUM   = IP_FACTURADOS.DOCNUM
INNER JOIN IP_USUARIOS_PREDIAL 
ON  IP_RECIBOS_DE_PAGO.COMPANIA     = IP_USUARIOS_PREDIAL.COMPANIA
AND IP_RECIBOS_DE_PAGO.PRECOD       = IP_USUARIOS_PREDIAL.CODIGO
AND IP_RECIBOS_DE_PAGO.NUMERO_ORDEN = IP_USUARIOS_PREDIAL.NUMERO_ORDEN
LEFT JOIN IP_BANCOS
ON  IP_RECIBOS_DE_PAGO.COMPANIA     = IP_BANCOS.COMPANIA
AND IP_RECIBOS_DE_PAGO.PAG_BANPAG   = IP_BANCOS.CODIGOBANCO 
WHERE IP_RECIBOS_DE_PAGO.COMPANIA = s$compania$s
  AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN = s$numeroOrden$s
  AND IP_USUARIOS_PREDIAL.INDICADOR_RESERVA NOT IN (0)
  AND IP_RECIBOS_DE_PAGO.ANULADO IN (0)
  AND IP_RECIBOS_DE_PAGO.PAGO NOT IN(0)
  AND TRUNC(IP_RECIBOS_DE_PAGO.PREFECPAG) BETWEEN s$fechaInicial$s AND s$fechaFinal$s
UNION
