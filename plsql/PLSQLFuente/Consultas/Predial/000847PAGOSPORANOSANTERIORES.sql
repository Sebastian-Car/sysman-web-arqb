SELECT 
  NUMCOM,
  CODPRED,
  NUMERO_ORDEN,
  FECHAACCESO,
  USUARIO,
  PAG_FEC,
  IP_BANCOS.NOMBREBANCO PAG_BAN,
  ANOFIN,
  PAG_VAL,
  INDACUERDO
FROM IP_BITPAGOSANOSANT
  LEFT JOIN IP_BANCOS
     ON IP_BITPAGOSANOSANT.COMPANIA = IP_BANCOS.COMPANIA
    AND IP_BITPAGOSANOSANT.PAG_BAN = IP_BANCOS.CODIGOBANCO
WHERE IP_BITPAGOSANOSANT.COMPANIA = s$compania$s
  AND IP_BITPAGOSANOSANT.PAG_FEC BETWEEN s$fechaInicio$s AND s$fechaFinal$s 
  AND IP_BITPAGOSANOSANT.PAG_BAN BETWEEN s$bancoInicio$s AND s$bancoFinal$s
