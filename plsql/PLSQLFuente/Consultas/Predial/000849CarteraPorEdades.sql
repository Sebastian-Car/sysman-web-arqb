SELECT IP_USUARIOS_PREDIAL.CODIGO,  
       IP_USUARIOS_PREDIAL.NOMBRE,  
       IP_FACTURADOS.PREANO,  
       IP_FACTURADOS.NOCOBRADO,  
       SYSDATE - TO_DATE('01/01/'||IP_FACTURADOS.PREANO, 'DD/MM/YYYY') DIAS, 
       (CASE WHEN SYSDATE - TO_DATE('01/01/'||IP_FACTURADOS.PREANO, 'DD/MM/YYYY') > 0 AND SYSDATE - TO_DATE('01/01/'||IP_FACTURADOS.PREANO, 'DD/MM/YYYY') < 181 THEN IP_FACTURADOS.TOTAL ELSE 0 END) PRIMERO, 
       (CASE WHEN SYSDATE - TO_DATE('01/01/'||IP_FACTURADOS.PREANO, 'DD/MM/YYYY') > 180 AND SYSDATE - TO_DATE('01/01/'||IP_FACTURADOS.PREANO, 'DD/MM/YYYY') < 361 THEN  IP_FACTURADOS.TOTAL ELSE 0 END)  SEGUNDO,  
       (CASE WHEN  SYSDATE - TO_DATE('01/01/'||IP_FACTURADOS.PREANO, 'DD/MM/YYYY') > 360 THEN IP_FACTURADOS.TOTAL ELSE 0 END)  TERCERO,  
       IP_USUARIOS_PREDIAL.NUMERO_ORDEN,  
      IP_FACTURADOS.TOTAL,  
       IP_FACTURADOS.PAGADO,  
       IP_FACTURADOS.INDCAR, 
       IP_USUARIOS_PREDIAL.M_INMOBILIARIA 
FROM   IP_FACTURADOS   
LEFT JOIN IP_USUARIOS_PREDIAL  
ON     IP_FACTURADOS.COMPANIA = IP_USUARIOS_PREDIAL.COMPANIA 
AND    IP_FACTURADOS.CODIGO = IP_USUARIOS_PREDIAL.CODIGO 
AND    IP_FACTURADOS.NUMERO_ORDEN = IP_USUARIOS_PREDIAL.NUMERO_ORDEN  
WHERE  IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s 
AND    IP_USUARIOS_PREDIAL.CODIGO BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'   
AND    IP_USUARIOS_PREDIAL.NUMERO_ORDEN = 's$numeroOrden$s'  
AND    IP_FACTURADOS.PREANO > IP_USUARIOS_PREDIAL.PAGO_ANO  
AND    IP_FACTURADOS.NOCOBRADO = 0 
AND    IP_FACTURADOS.TOTAL NOT IN (0)  
AND    IP_USUARIOS_PREDIAL.TOTAL >= s$masDeCuanto$s 
AND    IP_FACTURADOS.PAGADO = 0  
AND    IP_FACTURADOS.INDCAR = 0  
AND    IP_USUARIOS_PREDIAL.INDBORRADO = 0 
AND    IP_USUARIOS_PREDIAL.CODIGO_NO_ACTIVO = 0 
AND    IP_FACTURADOS.INDEXE = 0  
AND    IP_USUARIOS_PREDIAL.INDEXE = 0 
AND    IP_USUARIOS_PREDIAL.INDCAR = 0 
AND    (IP_FACTURADOS.INDPAGO_ACPAG = 0 OR IP_FACTURADOS.INDPAGO_ACPAG IS NULL )  
AND    (CASE WHEN IP_USUARIOS_PREDIAL.EXINICIAL IS NOT NULL THEN IP_FACTURADOS.PREANO  ELSE IP_USUARIOS_PREDIAL.EXINICIAL END) NOT IN (0)
