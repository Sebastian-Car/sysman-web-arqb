SELECT 
    IP_FACTURADOS.CODIGO, 
    IP_FACTURADOS.NUMERO_ORDEN, 
    IP_FACTURADOS.DOCNUM, 
    TO_CHAR(IP_FACTURADOS.FECHAPAGO,'DD/MM/YYYY') FECHAPAGO, 
 
    IP_BANCOS.NOMBREBANCO, 
    IP_FACTURADOS.PREVAL, 
    IP_FACTURADOS.PREANO, 
    IP_FACTURADOS.AVALUO 
FROM 
    IP_FACTURADOS 
        INNER JOIN IP_BANCOS 
        ON 
            IP_FACTURADOS.COMPANIA = IP_BANCOS.COMPANIA
                AND 
            IP_FACTURADOS.PAG_BAN = IP_BANCOS.CODIGOBANCO
WHERE 
    IP_FACTURADOS.COMPANIA = s$compania$s  
            AND 
    IP_FACTURADOS.CODIGO = 's$codigoPredio$s'
 
            AND 
 
    IP_FACTURADOS.NUMERO_ORDEN = 's$numeroOrden$s' 
  
            AND 
     UPPER(IP_FACTURADOS.OBSERVACIONES) IN  ('REGISTRO DE PAGO AÑO ANTERIOR ')
