SELECT 
  SUM(CASE WHEN DR.PREANO = TO_CHAR(SYSDATE, 'YYYY') THEN DR.Cs$concepto$s ELSE 0 END) ALUMACT, 
  SUM(CASE WHEN DR.PREANO = TO_CHAR(SYSDATE, 'YYYY') -1 THEN DR.Cs$concepto$s ELSE 0 END) ALUMANT, 
  SUM(CASE WHEN DR.PREANO < TO_CHAR(SYSDATE, 'YYYY') -1 THEN DR.Cs$concepto$s ELSE 0 END) ALUMANTS 
FROM IP_DETALLE_RECIBOPAGO DR
  INNER JOIN IP_RECIBOS_DE_PAGO RP
  ON  DR.COMPANIA = RP.COMPANIA
  AND DR.DOCNUM = RP.DOCNUM
WHERE DR.COMPANIA = 's$compania$s'
AND   RP.PAGO NOT IN (0)
AND   RP.ANULADO IN(0)
AND   TRUNC(RP.PREFECPAG) BETWEEN s$fechaInicial$s AND s$fechaFinal$s
