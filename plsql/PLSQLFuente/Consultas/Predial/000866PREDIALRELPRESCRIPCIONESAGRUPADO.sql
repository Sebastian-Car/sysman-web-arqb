SELECT  
  IIP_FACTURADOS.CODIGO, 
  (MIN(IIP_FACTURADOS.PREANO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) ||' - '|| MAX(IIP_FACTURADOS.PREANO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM)) PREANO,
  SUM(C1+C2+C3+C4+C13+C14+C15+C16+C17+C18+C19+C20) TOTALP, 
  SUM(IIP_FACTURADOS.C1) C1, 
  SUM(IIP_FACTURADOS.C2) C2, 
  SUM(IIP_FACTURADOS.C3) C3, 
  SUM(IIP_FACTURADOS.C4) C4, 
  SUM(IIP_FACTURADOS.C13) C13, 
  SUM(IIP_FACTURADOS.C14) C14, 
  SUM(IIP_FACTURADOS.C15) C15, 
  SUM(IIP_FACTURADOS.C16) C16, 
  SUM(IIP_FACTURADOS.C17) C17, 
  SUM(IIP_FACTURADOS.C18) C18, 
  SUM(IIP_FACTURADOS.C19) C19, 
  SUM(IIP_FACTURADOS.C20) C20
FROM  (s$consulta$s)  IIP_FACTURADOS
GROUP BY IIP_FACTURADOS.CODIGO  
