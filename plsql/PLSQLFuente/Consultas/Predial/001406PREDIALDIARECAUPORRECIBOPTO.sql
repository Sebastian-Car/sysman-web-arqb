FROM IP_RECIBOS_DE_PAGO RP 
	INNER JOIN IP_BANCOS BANCOS 
	    ON  RP.COMPANIA     = BANCOS.COMPANIA 
	    AND RP.PAG_BANPAG   = BANCOS.CODIGOBANCO
	INNER JOIN IP_USUARIOS_PREDIAL UP 
		ON  UP.COMPANIA     = RP.COMPANIA 
		AND UP.CODIGO       = RP.PRECOD 
		AND UP.NUMERO_ORDEN = RP.NUMERO_ORDEN 
WHERE UP.COMPANIA        = s$compania$s
  AND UP.NUMERO_ORDEN = 's$numeroOrden$s'
  AND RP.PREFECPAG     BETWEEN s$fechaInicial$s AND s$fechaFinal$s  
  AND RP.PAG_BANPAG   BETWEEN 's$bancoInicial$s' AND 's$bancoFinal$s'  
  AND RP.PAQUETEPAG   BETWEEN 's$paqueteInicial$s' AND 's$paqueteFinal$s' 
  AND RP.ANULADO      IN (0) 
  AND RP.PAGO         NOT IN (0) 
