SELECT  IP_TARIFAS.TRPRAN,
        IP_TARIFAS.TRPRAN1,
        IP_TARIFAS.TRPRAN2,
        IP_USUARIOS_PREDIAL.COMPANIA,
        IP_USUARIOS_PREDIAL.CODIGO,
        IP_USUARIOS_PREDIAL.NUMERO_ORDEN,
        IP_USUARIOS_PREDIAL.NIT,
        IP_USUARIOS_PREDIAL.DIRECCION,
        IP_USUARIOS_PREDIAL.AREA_HA,
        IP_USUARIOS_PREDIAL.AREA_M2,
        IP_USUARIOS_PREDIAL.AREA_CONSTRUIDA,
        IP_USUARIOS_PREDIAL.PAGO_ANO,
        IP_USUARIOS_PREDIAL.PAG_VAL,
        NVL(TO_CHAR(IP_USUARIOS_PREDIAL.PAG_FEC, 'DD/MM/YYYY'),'No registrada') PAG_FEC,
        IP_USUARIOS_PREDIAL.NUM_COM,
        IP_USUARIOS_PREDIAL.DIRECCION_CORRESPONDENCIA,
        IP_USUARIOS_PREDIAL.COD_POSTAL,
        IP_USUARIOS_PREDIAL.RECIBO_ACTUAL,
        IP_USUARIOS_PREDIAL.NOMBRE,
        s$fecLim$s PREFECLIM,
        SUM(IP_FACTURADOS.TOTAL) PREVAL,
        'Factura Informativa' INFO,
        s$docNum$s DOCNUM,
        SUBSTR(CHR(205)  ||CHR(102)  ||CHR(40)  ||'415'  ||CHR(41)  ||s$strCodigoEAN$s  ||CHR(40)  ||'8020'  ||CHR(41)  ||IP_USUARIOS_PREDIAL.CODIGO  ||SUBSTR(s$docNum$s,1,9)  ||CHR(40)  ||'3900'  ||CHR(41)  ||LPAD(0,14,0)  ||CHR(40)  ||'96'  ||CHR(41)  ||TO_CHAR(TO_DATE (s$fecLim$s,'DD/MM/YYYY'),'YYYYMMDD'),3) PIEBARRA,
        TO_CHAR(PCK_CODIGODEBARRAS.FC_IMPRIMIRCODIGODEBARRAS(CHR(205)  ||CHR(102)  ||'415'  ||s$strCodigoEAN$s  ||'8020'  ||IP_USUARIOS_PREDIAL.CODIGO  ||SUBSTR(s$docNum$s,1,9)  ||CHR(102)  ||'3900'  ||LPAD(0,14,0)  ||CHR(102)  ||'96'  ||TO_CHAR(TO_DATE (s$fecLim$s,'DD/MM/YYYY'),'YYYYMMDD'))) CODIGODEBARRAS_BIN
FROM    IP_USUARIOS_PREDIAL
  LEFT JOIN IP_FACTURADOS
    ON  IP_USUARIOS_PREDIAL.COMPANIA = IP_FACTURADOS.COMPANIA
    AND IP_USUARIOS_PREDIAL.CODIGO = IP_FACTURADOS.CODIGO
    AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN = IP_FACTURADOS.NUMERO_ORDEN
  INNER JOIN IP_TARIFAS
    ON  IP_USUARIOS_PREDIAL.COMPANIA = IP_TARIFAS.COMPANIA
    AND IP_USUARIOS_PREDIAL.TRPCOD   = IP_TARIFAS.TRPCOD
    AND IP_USUARIOS_PREDIAL.TRPRAN   = IP_TARIFAS.TRPRAN
    AND IP_USUARIOS_PREDIAL.TRPANO   = IP_TARIFAS.TRPANO
WHERE   IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s
  AND   IP_USUARIOS_PREDIAL.CODIGO   = s$codigo$s
  AND   IP_USUARIOS_PREDIAL.NUMERO_ORDEN = s$numeroOrden$s
  AND   IP_FACTURADOS.PAGADO            IN (0)
  AND   IP_FACTURADOS.NOCOBRADO    IN (0)
  s$condicionSub$s
GROUP BY IP_USUARIOS_PREDIAL.COMPANIA,
        IP_USUARIOS_PREDIAL.CODIGO,
        IP_USUARIOS_PREDIAL.NUMERO_ORDEN,
        IP_TARIFAS.TRPRAN,
        IP_TARIFAS.TRPRAN1,
        IP_TARIFAS.TRPRAN2,
        IP_USUARIOS_PREDIAL.NIT,
        IP_USUARIOS_PREDIAL.DIRECCION,
        IP_USUARIOS_PREDIAL.AREA_HA,
        IP_USUARIOS_PREDIAL.AREA_M2,
        IP_USUARIOS_PREDIAL.AREA_CONSTRUIDA,
        IP_USUARIOS_PREDIAL.PAGO_ANO,
        IP_USUARIOS_PREDIAL.PAG_VAL,
        TO_CHAR(IP_USUARIOS_PREDIAL.PAG_FEC, 'DD/MM/YYYY'),
        IP_USUARIOS_PREDIAL.NUM_COM,
        IP_USUARIOS_PREDIAL.DIRECCION_CORRESPONDENCIA,
        IP_USUARIOS_PREDIAL.COD_POSTAL,
        IP_USUARIOS_PREDIAL.RECIBO_ACTUAL,
        IP_USUARIOS_PREDIAL.NOMBRE
