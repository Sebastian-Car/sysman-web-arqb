WHERE IP_RESOLUCIONES_TIPO_3.COMPANIA = s$compania$s 
     AND IP_RESOLUCIONES_TIPO_3.RESOLUCION BETWEEN s$resolucionInicial$s AND s$resolucionFinal$s
