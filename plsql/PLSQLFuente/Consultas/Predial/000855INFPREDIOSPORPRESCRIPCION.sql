SELECT IP_USUARIOS_PREDIAL.CODIGO, 
       IP_USUARIOS_PREDIAL.NOMBRE, 
       IP_FACTURADOS.PREANO, 
       IP_FACTURADOS.AVALUO, 
       IP_FACTURADOS.C1, 
       IP_FACTURADOS.C2, 
       IP_FACTURADOS.C3, 
       IP_FACTURADOS.C4, 
       IP_FACTURADOS.C13, 
       IP_FACTURADOS.C14, 
       IP_FACTURADOS.C15, 
       IP_FACTURADOS.C16, 
       IP_FACTURADOS.C17, 
       IP_FACTURADOS.C18, 
       IP_FACTURADOS.TOTAL, 
       (IP_FACTURADOS.C14 + IP_FACTURADOS.C15 + IP_FACTURADOS.C16 + IP_FACTURADOS.C17 + IP_FACTURADOS.C18 + IP_FACTURADOS.C19 + IP_FACTURADOS.C20)  V_OTROS 
FROM   IP_USUARIOS_PREDIAL 
INNER JOIN IP_FACTURADOS 
ON    IP_USUARIOS_PREDIAL.COMPANIA = IP_FACTURADOS.COMPANIA  
AND   IP_USUARIOS_PREDIAL.CODIGO = IP_FACTURADOS.CODIGO 
AND   IP_USUARIOS_PREDIAL.NUMERO_ORDEN = IP_FACTURADOS.NUMERO_ORDEN 
WHERE IP_FACTURADOS.COMPANIA = s$compania$s
AND   IP_FACTURADOS.CODIGO BETWEEN 's$codigoInicial$s' AND  's$codigoFinal$s' 
AND   IP_FACTURADOS.NUMERO_ORDEN = 's$numeroOrden$s'
s$condicionAno$s
AND   IP_FACTURADOS.PAGADO = 0 
AND   IP_FACTURADOS.INDPAGO_ACPAG = 0 
AND   IP_FACTURADOS.NOCOBRADO = 0
ORDER BY 
      IP_USUARIOS_PREDIAL.CODIGO, 
      IP_FACTURADOS.PREANO DESC
