SELECT 
 IP_RECIBO_EXCEDENTES.COMPANIA
       , IP_RECIBO_EXCEDENTES.ANO_CAUSO
       , IP_RECIBO_EXCEDENTES.RESOLUCION
       , IP_RECIBO_EXCEDENTES.VALOR C_VALOR
       , IP_RECIBO_EXCEDENTES.C1
       , PCK_PREDIAL_COM4.FC_NOMBRE_CONCEPTO_PREDIAL(
                    UN_COMPANIA => IP_RECIBO_EXCEDENTES.COMPANIA
                   ,UN_CODIGO   => 1
                   ,UN_ANIO     => EXTRACT(YEAR FROM SYSDATE)) NOMBRECONCEPTO1
       , IP_RECIBO_EXCEDENTES.C2
       , PCK_PREDIAL_COM4.FC_NOMBRE_CONCEPTO_PREDIAL(
                    UN_COMPANIA => IP_RECIBO_EXCEDENTES.COMPANIA
                   ,UN_CODIGO   => 2
                   ,UN_ANIO     => EXTRACT(YEAR FROM SYSDATE)) NOMBRECONCEPTO2
       , IP_RECIBO_EXCEDENTES.C3
       , PCK_PREDIAL_COM4.FC_NOMBRE_CONCEPTO_PREDIAL(
                    UN_COMPANIA => IP_RECIBO_EXCEDENTES.COMPANIA
                   ,UN_CODIGO   => 3
                   ,UN_ANIO     => EXTRACT(YEAR FROM SYSDATE)) NOMBRECONCEPTO3
       , IP_RECIBO_EXCEDENTES.C4
       , PCK_PREDIAL_COM4.FC_NOMBRE_CONCEPTO_PREDIAL(
                    UN_COMPANIA => IP_RECIBO_EXCEDENTES.COMPANIA
                   ,UN_CODIGO   => 4
                   ,UN_ANIO     => EXTRACT(YEAR FROM SYSDATE)) NOMBRECONCEPTO4
       , IP_RECIBO_EXCEDENTES.C13
       , PCK_PREDIAL_COM4.FC_NOMBRE_CONCEPTO_PREDIAL(
                    UN_COMPANIA => IP_RECIBO_EXCEDENTES.COMPANIA
                   ,UN_CODIGO   => 13
                   ,UN_ANIO     => EXTRACT(YEAR FROM SYSDATE)) NOMBRECONCEPTO13
       , IP_RECIBO_EXCEDENTES.C16
       , PCK_PREDIAL_COM4.FC_NOMBRE_CONCEPTO_PREDIAL(
                    UN_COMPANIA => IP_RECIBO_EXCEDENTES.COMPANIA
                   ,UN_CODIGO   => 16
                   ,UN_ANIO     => EXTRACT(YEAR FROM SYSDATE)) NOMBRECONCEPTO16
       , IP_RECIBO_EXCEDENTES.C17
       , PCK_PREDIAL_COM4.FC_NOMBRE_CONCEPTO_PREDIAL(
                    UN_COMPANIA => IP_RECIBO_EXCEDENTES.COMPANIA
                   ,UN_CODIGO   => 17
                   ,UN_ANIO     => EXTRACT(YEAR FROM SYSDATE)) NOMBRECONCEPTO17
       , IP_RECIBO_EXCEDENTES.C18
       , PCK_PREDIAL_COM4.FC_NOMBRE_CONCEPTO_PREDIAL(
                    UN_COMPANIA => IP_RECIBO_EXCEDENTES.COMPANIA
                   ,UN_CODIGO   => 18
                   ,UN_ANIO     => EXTRACT(YEAR FROM SYSDATE)) NOMBRECONCEPTO18
       , (IP_RECIBO_EXCEDENTES.C14
         +IP_RECIBO_EXCEDENTES.C15
         +IP_RECIBO_EXCEDENTES.C19
         +IP_RECIBO_EXCEDENTES.C20) OTROS
FROM IP_RECIBO_EXCEDENTES
WHERE IP_RECIBO_EXCEDENTES.COMPANIA       = s$compania$s
  AND IP_RECIBO_EXCEDENTES.CODIGO_PREDIO  = 's$predio$s'
  AND IP_RECIBO_EXCEDENTES.NUMERO_ORDEN   = 's$numeroOrden$s'
  AND IP_RECIBO_EXCEDENTES.NUMERO_FACTURA = 's$numeroFactura$s' 
  AND IP_RECIBO_EXCEDENTES.ANULADO        IN (0) 
  AND IP_RECIBO_EXCEDENTES.PAGO           IN (0)
