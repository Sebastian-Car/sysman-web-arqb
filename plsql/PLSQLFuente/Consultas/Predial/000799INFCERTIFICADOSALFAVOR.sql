SELECT 
  IP_PAGOSDOBLES.PRECOD,
  IP_PAGOSDOBLES.PREANO, 
  IP_PAGOSDOBLES.VALOR, 
  IP_USUARIOS_PREDIAL.TIPO_NIT, 
  IP_USUARIOS_PREDIAL.NIT,
  IP_USUARIOS_PREDIAL.NOMBRE, 
  IP_USUARIOS_PREDIAL.DIRECCION, 
  IP_PAGOSDOBLES.TIPO,
  IP_PAGOSDOBLES.OBSERVACIONES 
FROM IP_PAGOSDOBLES 
  INNER JOIN IP_USUARIOS_PREDIAL 
     ON IP_PAGOSDOBLES.COMPANIA     = IP_USUARIOS_PREDIAL.COMPANIA 
    AND IP_PAGOSDOBLES.PRECOD       = IP_USUARIOS_PREDIAL.CODIGO 
    AND IP_PAGOSDOBLES.NUMERO_ORDEN = IP_USUARIOS_PREDIAL.NUMERO_ORDEN 
WHERE IP_PAGOSDOBLES.COMPANIA = s$compania$s
  AND IP_PAGOSDOBLES.PRECOD = 's$preCod$s'
  AND IP_PAGOSDOBLES.TIPO='S' 
  AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN='s$nOrden$s'
  AND IP_PAGOSDOBLES.PAGO IN (0)
  AND IP_PAGOSDOBLES.ANULADO IN (0)
