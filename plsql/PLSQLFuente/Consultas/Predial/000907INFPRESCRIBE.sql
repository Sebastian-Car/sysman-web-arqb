SELECT 
   UP.CODIGO
  ,TIPOS_DOCUMENTOS.DESCRIPCION TIPO_NIT
  ,UP.NIT
  ,UP.NOMBRE
  ,FA.PREANO
  ,FA.AVALUO
  ,FA.TRPPOR
  ,FA.TOTAL
  ,FA.C1
  ,FA.C2
  ,FA.C3
  ,FA.C4
  ,FA.C13
  ,FA.C14
  ,FA.C15
  ,FA.C16
  ,FA.C17
  ,FA.C18
  ,FA.C19
  ,FA.C20
  ,FA.NOCOBRADO
  ,TRIM(TO_CHAR(FA.FECHAPRESCRIPCION, 'day'))   || 
   TO_CHAR(FA.FECHAPRESCRIPCION, ', DD "de "')  || 
   TRIM(TO_CHAR(FA.FECHAPRESCRIPCION, 'month')) ||  
   TO_CHAR(FA.FECHAPRESCRIPCION, '" de" YYYY') FECHAPRESCRIPCION
  ,FA.FECHAPRESCRIPCION FECHA
FROM IP_FACTURADOS FA 
  INNER JOIN IP_USUARIOS_PREDIAL UP 
     ON FA.COMPANIA     = UP.COMPANIA
    AND FA.NUMERO_ORDEN = UP.NUMERO_ORDEN 
    AND FA.CODIGO       = UP.CODIGO  
  INNER JOIN TIPOS_DOCUMENTOS 
     ON UP.COMPANIA = TIPOS_DOCUMENTOS.COMPANIA
    AND UP.TIPO_NIT = TIPOS_DOCUMENTOS.DCTO_IDENTIDAD
WHERE UP.COMPANIA     = s$compania$s
  AND UP.CODIGO            Between 's$codInicial$s' AND 's$codFinal$s' 
  AND UP.NUMERO_ORDEN = 's$numeroOrden$s'
  AND TO_DATE(TRUNC(FA.FECHAPRESCRIPCION,'MI')) Between s$fechaIni$s AND s$fechaFin$s
  AND FA.NOCOBRADO NOT    IN (0)
  AND UP.CODIGO_NO_ACTIVO IN (0)
  AND UP.INDBORRADO       IN (0)
