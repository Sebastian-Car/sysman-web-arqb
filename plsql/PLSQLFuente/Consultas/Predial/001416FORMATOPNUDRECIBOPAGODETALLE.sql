SELECT FACTURADOS.CODIGO, 
       FACTURADOS.NUMERO_ORDEN, 
       FACTURADOS.PREANO, 
       FACTURADOS.TRPPOR, 
       FACTURADOS.AVALUO, 
       FACTURADOS.C1, 
       FACTURADOS.C2, 
       FACTURADOS.C3, 
       FACTURADOS.C4, 
       FACTURADOS.C13, 
       FACTURADOS.C14, 
       FACTURADOS.C15,        
       FACTURADOS.C16 +FACTURADOS.C17 + FACTURADOS.C18 + FACTURADOS.C19 AS SUMACPTOS, 
       FACTURADOS.C20, 
       FACTURADOS.TOTAL
 FROM IP_FACTURADOS FACTURADOS
WHERE FACTURADOS.COMPANIA                = s$compania$s
    AND FACTURADOS.CODIGO                     = s$codigoPredial$s
    AND FACTURADOS.PAGADO           	      IN(0)
    AND FACTURADOS.INDPAGO_ACPAG      IN(0)
    AND (s$anoUnico$s IN(0)  AND FACTURADOS.PREANO <= s$anoFin$s)
     OR (s$anoUnico$s IN(-1) AND FACTURADOS.PREANO  = s$anoFin$s)
ORDER BY FACTURADOS.PREANO
