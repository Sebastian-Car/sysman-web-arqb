SELECT DR.PREANO,
  DR.C1,
  DR.C2,
  DR.C3,
  DR.C4,
  DR.C13,
  DR.C14,
  DR.C15,
  DR.C16,
  DR.C17,
  DR.C18,
  DR.C19,
  DR.C20,
  DR.TOTAL,
  RP.PRECOD AS CODIGO,
  RP.NUMERO_ORDEN,
  DR.AVALUO,
  DR.TRPPOR,
  DR.C1 + DR.C3 AS IMPUESTO, 
  DR.C2 + DR.C4 AS INTERES, 
  DR.C16 + DR.C17 + DR.C18 + DR.C19 AS OTROS,
  DR.C14 + DR.C15 + DR.C18 + DR.C19 AS OTROS2
FROM IP_DETALLE_RECIBOPAGO DR
INNER JOIN IP_RECIBOS_DE_PAGO RP
ON DR.COMPANIA           = RP.COMPANIA
AND DR.DOCNUM            = RP.DOCNUM
WHERE RP.COMPANIA 		 = s$compania$s
     AND RP.PRECOD 		 = $P!{PR_CODIGO}
     AND RP.NUMERO_ORDEN 	 = $P!{PR_NUMERO_ORDEN}
     AND NVL(RP.PAGO,0) 		 = 0
