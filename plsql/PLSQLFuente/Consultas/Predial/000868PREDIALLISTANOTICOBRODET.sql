SELECT  IP_NOTIFICAPREDIAL.NUMERO_ORDEN, 
    TO_CHAR(IP_NOTIFICAPREDIAL.FECHA_REGISTRO,'DD/MM/YYYY') FECHA_REGISTRO,
		IP_NOTIFICAPREDIAL.CONSECUTIVO, 
		IP_NOTIFICAPREDIAL.CODIGO, 
		IP_NOTIFICAPREDIAL.NIT, 
		IP_NOTIFICAPREDIAL.NOMBRE, 
		IP_USUARIOS_PREDIAL.DIRECCION, 
		IP_USUARIOS_PREDIAL.TELEFONO_PROPIETARIO, 
		IP_FACTURADOS.PREANO, 
		IP_FACTURADOS.C1, 
		IP_FACTURADOS.C2, 
		IP_FACTURADOS.C3, 
		IP_FACTURADOS.C4, 
		IP_FACTURADOS.C13, 
		IP_FACTURADOS.C14, 
		IP_FACTURADOS.C15, 
		IP_FACTURADOS.C16, 
		IP_FACTURADOS.C17, 
		IP_FACTURADOS.C18, 
		IP_FACTURADOS.C19, 
		IP_FACTURADOS.C20, 
		IP_FACTURADOS.TOTAL, 
		IP_USUARIOS_PREDIAL.BLOQUEADO, 
		IP_USUARIOS_PREDIAL.INDBORRADO, 
		IP_FACTURADOS.PAGADO
          FROM IP_NOTIFICAPREDIAL 
          INNER JOIN (IP_USUARIOS_PREDIAL 
            INNER JOIN IP_FACTURADOS ON 
            IP_USUARIOS_PREDIAL.COMPANIA = IP_FACTURADOS.COMPANIA AND
            IP_USUARIOS_PREDIAL.CODIGO = IP_FACTURADOS.CODIGO AND
            IP_USUARIOS_PREDIAL.NUMERO_ORDEN = IP_FACTURADOS.NUMERO_ORDEN
            ) ON 
          IP_NOTIFICAPREDIAL.COMPANIA = IP_USUARIOS_PREDIAL.COMPANIA AND
           IP_NOTIFICAPREDIAL.CODIGO = IP_USUARIOS_PREDIAL.CODIGO AND
          IP_NOTIFICAPREDIAL.NUMERO_ORDEN = IP_USUARIOS_PREDIAL.NUMERO_ORDEN
          WHERE
          IP_NOTIFICAPREDIAL.COMPANIA = s$compania$s
          AND IP_NOTIFICAPREDIAL.NUMERO_ORDEN = 's$numeroOrden$s'
          AND IP_FACTURADOS.PREANO= IP_NOTIFICAPREDIAL.ANOINI
          AND IP_USUARIOS_PREDIAL.INDBORRADO IN (0) 
          AND IP_FACTURADOS.PAGADO IN (0) 
          s$condAdicional$s
          ORDER BY 
          IP_NOTIFICAPREDIAL.FECHA_REGISTRO DESC
