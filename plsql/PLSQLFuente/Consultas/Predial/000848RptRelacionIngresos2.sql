  IP_RECIBOS_DE_PAGO.DOCNUM,
  IP_RECIBOS_DE_PAGO.PRECOD,
  IP_USUARIOS_PREDIAL.NOMBRE,
  IP_RECIBOS_DE_PAGO.PREANO,
  IP_RECIBOS_DE_PAGO.C1 PRESENTEANO,
  IP_RECIBOS_DE_PAGO.C5 ANOPASADO, 
  IP_RECIBOS_DE_PAGO.C9 ANOSANTERIORES, 
  IP_RECIBOS_DE_PAGO.C2+IP_RECIBOS_DE_PAGO.C6+IP_RECIBOS_DE_PAGO.C10 INTERESESPRE,
  IP_RECIBOS_DE_PAGO.C4+IP_RECIBOS_DE_PAGO.C8+IP_RECIBOS_DE_PAGO.C12 INTERESESCAR, 
  IP_RECIBOS_DE_PAGO.C14+IP_RECIBOS_DE_PAGO.C15 SOBRETASA,
  IP_RECIBOS_DE_PAGO.C18 BOMBEROS, 
  IP_RECIBOS_DE_PAGO.C19 SALDOSYDEV, 
  IP_RECIBOS_DE_PAGO.C13 DESCUENTOS,
  IP_RECIBOS_DE_PAGO.C1+IP_RECIBOS_DE_PAGO.C2+IP_RECIBOS_DE_PAGO.C5+IP_RECIBOS_DE_PAGO.C6+IP_RECIBOS_DE_PAGO.C9+IP_RECIBOS_DE_PAGO.C10+IP_RECIBOS_DE_PAGO.C13+IP_RECIBOS_DE_PAGO.C15+IP_RECIBOS_DE_PAGO.C16+IP_RECIBOS_DE_PAGO.C17+IP_RECIBOS_DE_PAGO.C18+IP_RECIBOS_DE_PAGO.C20+IP_RECIBOS_DE_PAGO.C14 TOTALPREDIAL, 
  IP_RECIBOS_DE_PAGO.C3+IP_RECIBOS_DE_PAGO.C4+IP_RECIBOS_DE_PAGO.C7+IP_RECIBOS_DE_PAGO.C8+IP_RECIBOS_DE_PAGO.C11+IP_RECIBOS_DE_PAGO.C12+s$valorCar$s TOTALCAR,
  IP_RECIBOS_DE_PAGO.PREFECPAG,
  IP_RECIBOS_DE_PAGO.PAG_BANPAG,
  IP_RECIBOS_DE_PAGO.PREFEC, 
  IP_RECIBOS_DE_PAGO.C3 CARACTUAL,
  IP_RECIBOS_DE_PAGO.C7 CARANTE,
  IP_RECIBOS_DE_PAGO.C11 CARANTRS,
  IP_RECIBOS_DE_PAGO.C17 SOBPREDIAL, 
  IP_RECIBOS_DE_PAGO.C14, 
  IP_RECIBOS_DE_PAGO.C16,
  IP_RECIBOS_DE_PAGO.C18, 
  IP_RECIBOS_DE_PAGO.PREVAL,
  IP_RECIBOS_DE_PAGO.C1+IP_RECIBOS_DE_PAGO.C2+IP_RECIBOS_DE_PAGO.C3+IP_RECIBOS_DE_PAGO.C4+IP_RECIBOS_DE_PAGO.C5+IP_RECIBOS_DE_PAGO.C6+IP_RECIBOS_DE_PAGO.C7+IP_RECIBOS_DE_PAGO.C8+IP_RECIBOS_DE_PAGO.C9+IP_RECIBOS_DE_PAGO.C10+IP_RECIBOS_DE_PAGO.C11+IP_RECIBOS_DE_PAGO.C12+IP_RECIBOS_DE_PAGO.C13+IP_RECIBOS_DE_PAGO.C16+IP_RECIBOS_DE_PAGO.C17+IP_RECIBOS_DE_PAGO.C18+IP_RECIBOS_DE_PAGO.C20+IP_RECIBOS_DE_PAGO.C15+IP_RECIBOS_DE_PAGO.C19+IP_RECIBOS_DE_PAGO.C14 TOTALPAGO, 
  IP_RECIBOS_DE_PAGO.C15,
  SUMA_SOBRETASA.SUMADEC15,
  IP_RECIBOS_DE_PAGO.INTERES_ACUERDO,
  IP_RECIBOS_DE_PAGO.INTERES_RECARGO,
  IP_BANCOS.NOMBREBANCO
FROM IP_USUARIOS_PREDIAL 
  INNER JOIN IP_RECIBOS_DE_PAGO 
     ON IP_USUARIOS_PREDIAL.COMPANIA = IP_RECIBOS_DE_PAGO.COMPANIA
    AND IP_USUARIOS_PREDIAL.CODIGO = IP_RECIBOS_DE_PAGO.PRECOD
    AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN = IP_RECIBOS_DE_PAGO.NUMERO_ORDEN
  INNER JOIN (SELECT 
                IP_RECIBOS_DE_PAGO.DOCNUM,
                IP_RECIBOS_DE_PAGO.PRECOD,
                SUM(IP_FACTURADOS.C15) SUMADEC15
              FROM IP_RECIBOS_DE_PAGO 
                LEFT JOIN IP_FACTURADOS 
                   ON IP_RECIBOS_DE_PAGO.COMPANIA = IP_FACTURADOS.COMPANIA
                  AND IP_RECIBOS_DE_PAGO.DOCNUM = IP_FACTURADOS.DOCNUM
                  AND IP_RECIBOS_DE_PAGO.PRECOD = IP_FACTURADOS.CODIGO    
                  AND IP_RECIBOS_DE_PAGO.NUMERO_ORDEN = IP_FACTURADOS.NUMERO_ORDEN
              WHERE IP_RECIBOS_DE_PAGO.COMPANIA = s$compania$s
                AND IP_RECIBOS_DE_PAGO.PAGO NOT IN (0)
                AND IP_RECIBOS_DE_PAGO.ANULADO IN (0)
              GROUP BY IP_RECIBOS_DE_PAGO.DOCNUM, IP_RECIBOS_DE_PAGO.PRECOD) SUMA_SOBRETASA 
    ON IP_RECIBOS_DE_PAGO.DOCNUM = SUMA_SOBRETASA.DOCNUM
  INNER JOIN IP_BANCOS
     ON IP_RECIBOS_DE_PAGO.COMPANIA = IP_BANCOS.COMPANIA
    AND IP_RECIBOS_DE_PAGO.PAG_BANPAG = IP_BANCOS.CODIGOBANCO
WHERE IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s
  AND IP_RECIBOS_DE_PAGO.PREFECPAG BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY') 
  AND IP_RECIBOS_DE_PAGO.PAG_BANPAG BETWEEN 's$bancoInicial$s' AND 's$bancoFinal$s'
  AND IP_RECIBOS_DE_PAGO.ANULADO IN (0)
  AND IP_RECIBOS_DE_PAGO.PAGO NOT IN (0)
  AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN = 's$nOrden$s'
ORDER BY IP_RECIBOS_DE_PAGO.PREFECPAG
