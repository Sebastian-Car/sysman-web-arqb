SELECT IP_CERTIFICADOVALORIZACION.NUMCER, 
	IP_CERTIFICADOVALORIZACION.PREDIO CODIGOPREDIO, 
	IP_CERTIFICADOVALORIZACION.NOMBRE,
	TO_DATE(TO_CHAR(IP_CERTIFICADOVALORIZACION.FECHA_EXPEDICION,'DD/MM/YYYY')) FECHA_IMPRESO, 
	TO_CHAR(IP_CERTIFICADOVALORIZACION.FECHA_EXPEDICION,'HH:MM:SS' )HORA_IMPRESO, 
	IP_CERTIFICADOVALORIZACION.CREATED_BY IMPRESO_POR,
	IP_DESTINO_DOCUMENTOS.DESCRIPCION
FROM IP_CERTIFICADOVALORIZACION 
	INNER JOIN IP_DESTINO_DOCUMENTOS 
		ON IP_CERTIFICADOVALORIZACION.COMPANIA = IP_DESTINO_DOCUMENTOS.COMPANIA
		AND IP_CERTIFICADOVALORIZACION.DESTINO = IP_DESTINO_DOCUMENTOS.CODIGO
WHERE IP_CERTIFICADOVALORIZACION.COMPANIA = s$compania$s 
AND TRUNC(IP_CERTIFICADOVALORIZACION.FECHA_EXPEDICION)  Between s$fechaInicial$s And s$fechaFinal$s
AND IP_CERTIFICADOVALORIZACION.DESTINO Between 's$destinoInicial$s' And 's$destinoFinal$s' 
ORDER BY FECHA_IMPRESO
