SELECT 
    IP_TMP_FACTURADOSACUERDOS.PREDIO, 
    IP_USUARIOS_PREDIAL.NOMBRE, 
    IP_TMP_ACUERDOS.PREANOI, 
    IP_TMP_ACUERDOS.PREANO, 
    CASE WHEN 
         IP_TMP_FACTURADOSACUERDOS.CUOTA = 0
    THEN 
        'ABONO'
    ELSE
          '' || IP_TMP_FACTURADOSACUERDOS.CUOTA || ''
    END  NCUOTA, 
    IP_TMP_FACTURADOSACUERDOS.CUOTA, 
    IP_TMP_FACTURADOSACUERDOS.CAPITAL, 
    IP_TMP_FACTURADOSACUERDOS.INTERESES, 
    IP_TMP_FACTURADOSACUERDOS.INTERES_ACUERDO, 
    IP_TMP_FACTURADOSACUERDOS.TOTAL 
FROM 
    IP_TMP_FACTURADOSACUERDOS 
        INNER JOIN IP_USUARIOS_PREDIAL 
        ON IP_TMP_FACTURADOSACUERDOS.COMPANIA = IP_USUARIOS_PREDIAL.COMPANIA 
        AND  IP_TMP_FACTURADOSACUERDOS.PREDIO = IP_USUARIOS_PREDIAL.CODIGO 
        AND IP_TMP_FACTURADOSACUERDOS.NUMERO_ORDEN = IP_USUARIOS_PREDIAL.NUMERO_ORDEN 
   INNER JOIN IP_TMP_ACUERDOS 
    ON IP_TMP_FACTURADOSACUERDOS.COMPANIA = IP_TMP_ACUERDOS.COMPANIA  
  AND IP_TMP_FACTURADOSACUERDOS.CODIGOACUERDO = IP_TMP_ACUERDOS.CODIGOACUERDO  
 AND IP_TMP_FACTURADOSACUERDOS.PREDIO = IP_TMP_ACUERDOS.PREDIO  
 AND IP_TMP_FACTURADOSACUERDOS.NUMERO_ORDEN = IP_TMP_ACUERDOS.NUMERO_ORDEN 
WHERE 
          IP_TMP_FACTURADOSACUERDOS.COMPANIA =  s$compania$s  
  
 AND  IP_TMP_FACTURADOSACUERDOS.CODIGOACUERDO = 's$codigoAcuerdo$s'
