SELECT 
  IP_USUARIOS_PREDIAL.CODIGO, 
  IP_USUARIOS_PREDIAL.NUMERO_ORDEN, 
  IP_USUARIOS_PREDIAL.CLASE_PREDIO, 
  IP_USUARIOS_PREDIAL.PAGO_ANO,
  IP_USUARIOS_PREDIAL.PAG_VAL,
  IP_USUARIOS_PREDIAL.PAG_FEC,
  IP_USUARIOS_PREDIAL.PAG_BAN, 
  IP_USUARIOS_PREDIAL.AVALUO_ANO,
  IP_CLASE_PREDIOS.DESCRIPCION, 
  IP_USUARIOS_PREDIAL.NOMBRE,
  IP_USUARIOS_PREDIAL.DIRECCION
FROM IP_USUARIOS_PREDIAL
  INNER JOIN IP_CLASE_PREDIOS  
     ON IP_USUARIOS_PREDIAL.CLASE_PREDIO = IP_CLASE_PREDIOS.CODIGO
WHERE IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s
  AND IP_USUARIOS_PREDIAL.CODIGO BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
  AND IP_USUARIOS_PREDIAL.CLASE_PREDIO BETWEEN 's$claseInicial$s' AND 's$claseFinal$s'
ORDER BY IP_USUARIOS_PREDIAL.CODIGO
