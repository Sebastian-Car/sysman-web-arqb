SELECT  
        IP_ACUERDOS.FECHAACUERDO,
        IP_ACUERDOS.CODIGOACUERDO,
        IP_ACUERDOS.PREDIO,
        IP_ACUERDOS.ID_RESP,
        IP_ACUERDOS.NOMBRE_RESP,
        IP_ACUERDOS.DIRECCION_RESP,
        IP_ACUERDOS.TELEFONO_RESP,
        IP_ACUERDOS.RESOLUCION,
        IP_ACUERDOS.NCUOTAS,
        IP_ACUERDOS.MONTOCAPITAL,
        IP_ACUERDOS.MONTOINTERESES,
        SUM(IP_FACTURADOSACUERDOS.INTERES_ACUERDO) AS INT_ACUERDO,
        SUM(IP_FACTURADOSACUERDOS.INTERES_RECARGO) AS INT_RECARGO,
        NVL(IP_ACUERDOS.MONTOCAPITAL,0) +  NVL(IP_ACUERDOS.MONTOINTERESES,0) + SUM(NVL(IP_FACTURADOSACUERDOS.INTERES_ACUERDO,0))  + SUM(NVL(IP_FACTURADOSACUERDOS.INTERES_RECARGO,0)) TOTAL,
        IP_ACUERDOS.CANCELADO,
        IP_ACUERDOS.ANULADO
FROM IP_ACUERDOS
INNER JOIN IP_FACTURADOSACUERDOS
  ON IP_ACUERDOS.COMPANIA 		= IP_FACTURADOSACUERDOS.COMPANIA
  AND IP_ACUERDOS.CODIGOACUERDO 	= IP_FACTURADOSACUERDOS.CODIGOACUERDO
  AND IP_ACUERDOS.PREDIO 		= IP_FACTURADOSACUERDOS.PREDIO
  AND IP_ACUERDOS.NUMERO_ORDEN 		= IP_FACTURADOSACUERDOS.NUMERO_ORDEN
WHERE IP_ACUERDOS.COMPANIA = s$compania$s
AND IP_ACUERDOS.FECHAACUERDO BETWEEN s$fechaInicial$s AND s$fechaFinal$s
 s$estado$s
GROUP BY IP_ACUERDOS.CODIGOACUERDO,
        IP_ACUERDOS.PREDIO,
        IP_ACUERDOS.ID_RESP,
        IP_ACUERDOS.NOMBRE_RESP,
        IP_ACUERDOS.DIRECCION_RESP,
        IP_ACUERDOS.TELEFONO_RESP,
        IP_ACUERDOS.NCUOTAS,
        IP_ACUERDOS.MONTOCAPITAL,
        IP_ACUERDOS.MONTOINTERESES,
        IP_ACUERDOS.FECHAACUERDO,
        IP_ACUERDOS.CANCELADO,
        IP_ACUERDOS.ANULADO,
        IP_ACUERDOS.RESOLUCION
ORDER BY 	IP_ACUERDOS.CODIGOACUERDO, 
		IP_ACUERDOS.FECHAACUERDO
