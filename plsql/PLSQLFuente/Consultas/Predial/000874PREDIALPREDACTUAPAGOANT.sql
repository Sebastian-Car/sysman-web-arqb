SELECT IP_BITPAGOSANOSANT.NUMCOM,
  IP_USUARIOS_PREDIAL.CODIGO,
  IP_USUARIOS_PREDIAL.NOMBRE,
  IP_USUARIOS_PREDIAL.AREA_HA,
  IP_USUARIOS_PREDIAL.AREA_M2,
  IP_USUARIOS_PREDIAL.AREA_CONSTRUIDA,
  IP_USUARIOS_PREDIAL.AVALUO_ANO,
  IP_USUARIOS_PREDIAL.PAGO_ANO,
  IP_USUARIOS_PREDIAL.PAG_VAL,
  IP_USUARIOS_PREDIAL.DIRECCION,
  PCK_SYSMAN_UTL.FC_NOMBRE_MES(TO_CHAR(IP_BITPAGOSANOSANT.PAG_FEC, 'MM'))
  ||' - '
  || TO_CHAR(IP_BITPAGOSANOSANT.PAG_FEC, 'YYYY') MESPAG_FEC ,
  TO_CHAR(IP_BITPAGOSANOSANT.PAG_FEC, 'MM/YYYY') PAG_FEC ,
  TO_CHAR(IP_BITPAGOSANOSANT.PAG_FEC, 'YYYY') ANOPAGO,
  TO_NUMBER(TO_CHAR(IP_BITPAGOSANOSANT.PAG_FEC, 'MM')) MESPAGO,
  TO_CHAR(IP_BITPAGOSANOSANT.FECHAACCESO, 'DD-MM-YYYY') FECHAACCESO
FROM IP_BITPAGOSANOSANT
LEFT JOIN IP_RECIBOS_DE_PAGO
ON IP_BITPAGOSANOSANT.COMPANIA = IP_RECIBOS_DE_PAGO.COMPANIA
AND IP_BITPAGOSANOSANT.NUMCOM  = IP_RECIBOS_DE_PAGO.DOCNUM
INNER JOIN IP_USUARIOS_PREDIAL
ON IP_BITPAGOSANOSANT.COMPANIA      = IP_USUARIOS_PREDIAL.COMPANIA
AND IP_BITPAGOSANOSANT.CODPRED      = IP_USUARIOS_PREDIAL.CODIGO
AND IP_BITPAGOSANOSANT.NUMERO_ORDEN = IP_USUARIOS_PREDIAL.NUMERO_ORDEN
WHERE IP_USUARIOS_PREDIAL.COMPANIA  = 's$compania$s'
AND IP_BITPAGOSANOSANT.PAG_FEC BETWEEN TO_DATE(s$fechaInicio$s,'DD/MM/YYYY HH24:mi:ss') AND TO_DATE(s$fechaFin$s,'DD/MM/YYYY HH24:mi:ss')
AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN        = 's$numeroOrden$s'
AND IP_USUARIOS_PREDIAL.CODIGO_NO_ACTIVO   IN (0)
AND IP_USUARIOS_PREDIAL.INDBORRADO         IN (0)
AND IP_USUARIOS_PREDIAL.INDPAGOANOANTE NOT IN (0)
