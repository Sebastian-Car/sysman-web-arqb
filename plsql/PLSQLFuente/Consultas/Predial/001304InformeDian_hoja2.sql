SELECT '2' "Tipo de registro",
      '81' "Codigo de información",
      IP_USUARIOS_PREDIAL.TIPO "Codigo del bien a reportar",
      CASE WHEN INSTR(IP_USUARIOS_PREDIAL.NIT, '-') > 0 
        THEN SUBSTR(IP_USUARIOS_PREDIAL.NIT, 1, INSTR(IP_USUARIOS_PREDIAL.NIT, '-') - 1)
        ELSE IP_USUARIOS_PREDIAL.NIT
      END NIT,
      IP_USUARIOS_PREDIAL.TIPO_NIT "Clase de documento",
      IP_USUARIOS_PREDIAL.NOMBRE "Apellidos y nombres",
      IP_USUARIOS_PREDIAL.DIRECCION "Dirección del predio informado",
      IP_USUARIOS_PREDIAL.MUNICIPIO "Cód ubicación del predio",
      IP_USUARIOS_PREDIAL.M_INMOBILIARIA "Matricula inmobiliaria",
      IP_FACTURADOS.AVALUO "Valor avaluo del predio"      
FROM IP_USUARIOS_PREDIAL 
	INNER JOIN IP_TIPO_PREDIO 
	ON IP_USUARIOS_PREDIAL.TIPO = IP_TIPO_PREDIO.CODIGO 
	INNER JOIN IP_FACTURADOS 
	ON IP_USUARIOS_PREDIAL.COMPANIA = IP_FACTURADOS.COMPANIA
	AND IP_USUARIOS_PREDIAL.CODIGO = IP_FACTURADOS.CODIGO
WHERE IP_FACTURADOS.COMPANIA = s$compania$s
  AND IP_FACTURADOS.PREANO = s$anio$s
	AND IP_FACTURADOS.AVALUO >= s$valor$s
	s$condicion$s
