SELECT  IP_USUARIOS_PREDIAL.COMPANIA,
        IP_USUARIOS_PREDIAL.CODIGO,
        IP_FACTURADOS.PREANO,
        IP_USUARIOS_PREDIAL.NOMBRE,
        IP_USUARIOS_PREDIAL.NIT,
        IP_FACTURADOS.DESESPC2,
        IP_FACTURADOS.DESESPC4,
        IP_FACTURADOS.DOCNUM,
        IP_FACTURADOS.PREFEC,
        IP_USUARIOS_PREDIAL.M_INMOBILIARIA,
        IP_FACTURADOS.DESESPC2+ IP_FACTURADOS.DESESPC4 AS DESCUENTOESPECIAL
FROM IP_USUARIOS_PREDIAL
  INNER JOIN IP_FACTURADOS
    ON IP_USUARIOS_PREDIAL.COMPANIA        	= IP_FACTURADOS.COMPANIA
    AND IP_USUARIOS_PREDIAL.CODIGO        	= IP_FACTURADOS.CODIGO
    AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN 	= IP_FACTURADOS.NUMERO_ORDEN
WHERE IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s
	AND IP_USUARIOS_PREDIAL.CODIGO BETWEEN s$codigoInicial$s AND s$codigoFinal$s
	AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN = s$numeroOrden$s
  	AND IP_FACTURADOS.PREFEC BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  	AND  IP_FACTURADOS.DESESPC2 + IP_FACTURADOS.DESESPC4      NOT IN(0)
  	AND IP_FACTURADOS.PAGADO                                  NOT IN (0)
ORDER BY IP_USUARIOS_PREDIAL.CODIGO,
        IP_FACTURADOS.PREANO,
        IP_USUARIOS_PREDIAL.NOMBRE
