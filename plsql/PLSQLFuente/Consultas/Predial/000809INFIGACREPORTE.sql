SELECT  IP_IGAC_REPORTE.RESOLUCION, 
        IP_IGAC_REPORTE.ID_REP, 
        IP_IGAC_REPORTE.ANO, 
        IP_IGAC_REPORTE.CODIGO, 
        IP_IGAC_REPORTE.DESCRIPCION, 
        IP_IGAC_REPORTE.FECHA_REGISTRO,
        IP_USUARIOS_PREDIAL.M_INMOBILIARIA 
FROM IP_IGAC_REPORTE 
  INNER JOIN IP_USUARIOS_PREDIAL 
    ON  IP_IGAC_REPORTE.COMPANIA     = IP_USUARIOS_PREDIAL.COMPANIA 
    AND IP_IGAC_REPORTE.CODIGO       = IP_USUARIOS_PREDIAL.CODIGO 
    AND IP_IGAC_REPORTE.NUMERO_ORDEN = IP_USUARIOS_PREDIAL.NUMERO_ORDEN 
WHERE IP_IGAC_REPORTE.COMPANIA = s$compania$s 
  s$condicion$s
