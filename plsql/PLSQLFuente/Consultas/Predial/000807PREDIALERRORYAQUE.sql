SELECT IP_YAQUERROR.PREDIO,
 IP_YAQUERROR.CODIGOERROR, 
 IP_YAQUERROR.MENSAJE, 
 IP_YAQUERROR.FECHA, 
 IP_USUARIOS_PREDIAL.NOMBRE, 
 IP_USUARIOS_PREDIAL.NIT,
 IP_USUARIOS_PREDIAL.NUMERO_ORDEN
FROM IP_YAQUERROR LEFT JOIN 
IP_USUARIOS_PREDIAL ON 
IP_YAQUERROR.COMPANIA = IP_USUARIOS_PREDIAL.COMPANIA AND
IP_YAQUERROR.PREDIO = IP_USUARIOS_PREDIAL.CODIGO
WHERE 
IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s
AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN= '001'
ORDER BY IP_YAQUERROR.PREDIO
