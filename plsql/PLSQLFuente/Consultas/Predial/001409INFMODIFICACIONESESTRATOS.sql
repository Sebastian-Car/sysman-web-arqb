WITH AUDITORIA AS (
SELECT  IP_AUDITORIA.COMPANIA, 
        IP_AUDITORIA.MODCCO, 
        IP_AUDITORIA.MODCOD, 
        IP_AUDITORIA.MODFEC  FECHA,
        CASE WHEN MODCCO = '550' 
             THEN MODVAN
             ELSE ' '
        END  ESTRATO_ANT, 
        CASE WHEN MODCCO = '550' 
             THEN MODVNU
             ELSE ' '
        END  ESTRATO_NUEVO, 
        CASE WHEN MODCCO ='551'
             THEN MODVAN
             ELSE ' ' 
        END  CERTIF_ANT, 
        CASE WHEN MODCCO = '551'
             THEN MODVNU
             ELSE ' ' 
        END  CERTIF_NUEVO
 FROM IP_AUDITORIA
WHERE IP_AUDITORIA.COMPANIA      = s$compania$s
  AND (IP_AUDITORIA.MODCCO       = '550' OR IP_AUDITORIA.MODCCO = '551')
  AND TRUNC(IP_AUDITORIA.MODFEC) BETWEEN TO_DATE('s$fechaInicial$s', 'DD/MM/YY') AND  TO_DATE('s$fechaFinal$s','DD/MM/YY')
  AND IP_AUDITORIA.MODCOD        BETWEEN 's$predioInicial$s'  AND  's$predioFinal$s'
)
SELECT 	AUDITORIA.MODCCO, 
	SUBSTR(MODCOD,1,2) TIPOP, 
	SUBSTR(MODCOD,3,2) SECTOR, 
	SUBSTR(MODCOD,5,4) MANZANA, 
	SUBSTR(MODCOD,9,4) PREDIO,
	SUBSTR(MODCOD,13,3) MEJORA, 
	TO_CHAR(AUDITORIA.FECHA,'DD/MM/YYYY') FECHA , 
	IP_TABLA_ESTRATOS.NOMBRE_ESTRATO  ESTRATO_ANT, 
	TABLA_ESTRATOS_1.NOMBRE_ESTRATO   ESTRATO_NUEVO, 
	IP_FORMATO_CERTIFICADOS.NOMBRE    CERTIFICADO_ANT, 
	FORMATO_CERTIFICADOS_1.NOMBRE     CERTIFICADO_NUEVO
FROM AUDITORIA
LEFT JOIN IP_FORMATO_CERTIFICADOS 
	  ON AUDITORIA.COMPANIA      = IP_FORMATO_CERTIFICADOS.COMPANIA
	 AND AUDITORIA.CERTIF_ANT    = IP_FORMATO_CERTIFICADOS.FORMATO
LEFT JOIN IP_FORMATO_CERTIFICADOS  FORMATO_CERTIFICADOS_1 
	  ON AUDITORIA.COMPANIA      = FORMATO_CERTIFICADOS_1.COMPANIA 
	 AND AUDITORIA.CERTIF_NUEVO  = FORMATO_CERTIFICADOS_1.FORMATO 	 
LEFT JOIN IP_TABLA_ESTRATOS 
	  ON AUDITORIA.ESTRATO_ANT   = IP_TABLA_ESTRATOS.CODIGO_ESTRATO
LEFT JOIN IP_TABLA_ESTRATOS TABLA_ESTRATOS_1 
	  ON auditoria.ESTRATO_NUEVO = TABLA_ESTRATOS_1.CODIGO_ESTRATO
