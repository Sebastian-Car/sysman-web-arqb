SELECT RP.COMPANIA,
  RP.DOCNUM AS CODIGO,
  RP.NUMERO_ORDEN,
  D.PREANO,
  MAX(D.TRPPOR) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS TRPPOR,
  MAX(D.AVALUO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS AVALUO,
  SUM(D.C1)      AS C1,
  SUM(D.C3)      AS C3,
  SUM(D.C4)      AS C4,
  SUM(D.C2)      AS C2,
  SUM(D.C6)      AS C6,
  SUM(D.C8)      AS C8,
  SUM(D.C10)     AS C10,
  SUM(D.C12)     AS C12,
  SUM(D.C5)      AS C5,
  SUM(D.C17)     AS C7,
  SUM(D.C9)      AS C9,
  SUM(D.C11)     AS C11,
  SUM(D.C15)     AS C15,
  SUM(D.C16)     AS C16,
  SUM(D.C17)     AS C17,
  SUM(D.C18)     AS C18,
  SUM(D.C19)     AS C19,
  SUM(D.C20)     AS C20,
  SUM(D.C13)     AS C13,
  SUM(D.C14)     AS C14,
  SUM(D.TOTAL)   AS TOTAL,
  0              AS DESCAPARTE
FROM IP_DETALLE_RECIBOPAGO D
INNER JOIN IP_RECIBOS_DE_PAGO RP
ON RP.COMPANIA      = D.COMPANIA
AND RP.DOCNUM       = D.DOCNUM
WHERE D.COMPANIA    = $P{PR_COMPANIA}
AND D.DOCNUM        = $P{PR_CODIGO}
AND RP.NUMERO_ORDEN = $P{PR_NUMERO_ORDEN}
s$condicionSub$s
GROUP BY D.PREANO,
  0,
  RP.COMPANIA,
  RP.DOCNUM,
  RP.NUMERO_ORDEN
