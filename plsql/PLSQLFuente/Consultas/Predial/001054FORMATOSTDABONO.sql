SELECT 
  UP.CODIGO,
  UP.NIT,
  UP.NUM_COM,
  UP.PAGO_ANO,
  UP.PAG_VAL,
  CASE 
  WHEN UP.PAG_FEC IS NULL THEN '  '
  ELSE TO_CHAR(UP.PAG_FEC,'DD/MM/YYYY') 
  END PAG_FEC,
  UP.AREA_HA,
  UP.AREA_M2,
  UP.AREA_CONSTRUIDA,
  UP.NOMBRE,
  UP.DIRECCION,
  UP.DIRECCION_CORRESPONDENCIA,
  UP.NUMERO_ORDEN,
  UP.RECIBO_ACTUAL,
  UP.ESTRATO_SOCIOECONOMICO,
  UP.COD_POSTAL,
  RP.PREANO||' A '||PREANOI AS ANIOS_ABONADOS,
  RP.PREVAL AS SUMATOTAL,
  SUBSTR(CHR(205)  ||CHR(102)  ||CHR(40)  ||'415'  ||CHR(41)  ||s$strCodigoEAN$s||CHR(40)  ||'8020'  ||CHR(41)  ||CODIGO  ||SUBSTR(RP.DOCNUM,1,9)  ||CHR(40)  ||'3900'  ||CHR(41)  ||LPAD(RP.PREVAL,0)  ||CHR(40)  ||'96'  ||CHR(41)  ||TO_CHAR(RP.PREFECLIM,'YYYYMMDD'),3) AS PIEBARRA,
  TO_CHAR(PCK_CODIGODEBARRAS.FC_IMPRIMIRCODIGODEBARRAS(CHR(205)  ||CHR(102)  ||'415'  ||s$strCodigoEAN$s||'8020'  ||CODIGO  ||SUBSTR(RP.DOCNUM,1,9)  ||CHR(102)  ||'3900'  ||LPAD(RP.PREVAL,14,0)  ||CHR(102)  ||'96'  ||TO_CHAR(RP.PREFECLIM,'YYYYMMDD'))) AS CODIGODEBARRAS_BIN
FROM IP_USUARIOS_PREDIAL UP
INNER JOIN IP_RECIBOS_DE_PAGO RP
    ON UP.COMPANIA      = RP.COMPANIA
    AND UP.CODIGO       = RP.PRECOD 
    AND UP.NUMERO_ORDEN = RP.NUMERO_ORDEN
WHERE UP.COMPANIA    	       = s$compania$s
     AND UP.CODIGO          	       = s$codigo$s
     AND UP.NUMERO_ORDEN   = s$numeroOrden$s
     AND RP.DOCNUM 	       = s$docnum$s
