SELECT  IP_USUARIOS_PREDIAL.NOMBRE, 
        IP_USUARIOS_PREDIAL.NIT, 
        IP_USUARIOS_PREDIAL.CODIGO, 
        IP_USUARIOS_PREDIAL.NUMERO_ORDEN, 
        IP_USUARIOS_PREDIAL.DIRECCION, 
        IP_USUARIOS_PREDIAL.DESTINO_ECONOMICO, 
        IP_USUARIOS_PREDIAL.ESTRATO_SOCIOECONOMICO, 
        IP_USUARIOS_PREDIAL.AVALUO_ANO, 
        IP_USUARIOS_PREDIAL.AREA_M2, 
        IP_USUARIOS_PREDIAL.AREA_CONSTRUIDA, 
        ('RECIBO DE PAGO NO. ' || IP_USUARIOS_PREDIAL.NUM_COM || ' - ' || ' VALOR ' || TO_CHAR(IP_USUARIOS_PREDIAL.PAG_VAL, '999,999,900.00' ) || ' FECHA ' || TO_CHAR(IP_USUARIOS_PREDIAL.PAG_FEC,'DD/MM/YYYY')) AS PAGO, 
        IP_FACTURADOS.PREANO, 
        IP_FACTURADOS.PAGADO, 
        IP_FACTURADOS.NOCOBRADO, 
        IP_FACTURADOS.AVALUO, 
        IP_FACTURADOS.C1, 
        IP_FACTURADOS.C2, 
        IP_FACTURADOS.C3, 
        IP_FACTURADOS.C4, 
        IP_FACTURADOS.C5, 
        IP_FACTURADOS.C6, 
        IP_FACTURADOS.C7, 
        IP_FACTURADOS.C8, 
        IP_FACTURADOS.C9, 
        IP_FACTURADOS.C10, 
        IP_FACTURADOS.C11, 
        IP_FACTURADOS.C12, 
        IP_FACTURADOS.C13, 
        IP_FACTURADOS.C14, 
        IP_FACTURADOS.C15, 
        IP_FACTURADOS.C16, 
        IP_FACTURADOS.C17, 
        IP_FACTURADOS.C18, 
        IP_FACTURADOS.C19, 
        IP_FACTURADOS.C20, 
        IP_TARIFAS.TRPANO, 
        IP_TARIFAS.TRPPOR
FROM IP_USUARIOS_PREDIAL 
  INNER JOIN IP_TARIFAS 
    ON IP_USUARIOS_PREDIAL.COMPANIA       = IP_TARIFAS.COMPANIA
    AND IP_USUARIOS_PREDIAL.TRPCOD        = IP_TARIFAS.TRPCOD 
    AND IP_USUARIOS_PREDIAL.TRPANO        = IP_TARIFAS.TRPANO
    AND IP_USUARIOS_PREDIAL.TRPRAN        = IP_TARIFAS.TRPRAN
  INNER JOIN IP_FACTURADOS 
    ON IP_USUARIOS_PREDIAL.COMPANIA       = IP_FACTURADOS.COMPANIA
    AND IP_USUARIOS_PREDIAL.CODIGO        = IP_FACTURADOS.CODIGO
    AND IP_USUARIOS_PREDIAL.NUMERO_ORDEN  = IP_FACTURADOS.NUMERO_ORDEN 
WHERE IP_USUARIOS_PREDIAL.COMPANIA = s$compania$s
  s$condicion$s
ORDER BY IP_FACTURADOS.PREANO
