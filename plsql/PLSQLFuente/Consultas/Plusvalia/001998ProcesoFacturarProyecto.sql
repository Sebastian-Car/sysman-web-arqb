SELECT VPF.NUMERO_ACTOADMINISTRATIVO,
VF.NUMERO_FACTURA,
TO_CHAR(SYSDATE,'DD/MM/YYY') FECHA_EXPEDICION,
TO_CHAR(SYSDATE + 5,'DD/MM/YYY') FECHA_VENCIMIENTO,
VP.VALOR_DISTRIBUIBLE,
VF.VALOR VALOR_TOTAL,
IUP.NOMBRE NOMBRE_PROPIETARIO,
VF.IP_CODIGO CEDULA_CATASTRAL,
VB.FACTOR_GRADO_BENEFICIO,
VP_BEN.NOMBRE GRADO_BENEFICIO,
VB.FACTOR_DESTINACION_ECONOMICA,
VP_DES.NOMBRE DESTINACION_ECONOMICA,
VB.AREA_FISICA AREA_BRUTA,
VF.ESTADO,
VB.MATRICULA_INMOBILIARIA,
VB.DIRECCION,
VB.DIRECCION_CORRESPONDENCIA,
VB.AREA_AFECTADA,
VB.AREA_GRAVADA
,
VP.AREA_TOTAL AREA_VIRTUAL,
VB.AREA_GRAVADA * VB.FACTOR_DESTINACION_ECONOMICA * VB.FACTOR_GRADO_BENEFICIO AREA_AMPLIADA,
    SUBSTR(CHR(205)
        || CHR(102)
        || CHR(40)
        || '415'
        || CHR(41)
        || 's$codigoEan$s'
        || CHR(40)
        || '8020'
        || CHR(41)
        || LPAD(NVL(VP.CLASE,'0'),5,0)
        || LPAD(VF.NUMERO_FACTURA,19,0)
        || CHR(40)
        || '3900'
        || CHR(41)
        || LPAD(VF.VALOR,14,0)
        || CHR(40)
        || '96'
        || CHR(41)
        || TO_CHAR(SYSDATE+ 5 ,'YYYYMMDD'),3) AS PIEBARRA,
        
        TO_CHAR(PCK_CODIGODEBARRAS.FC_IMPRIMIRCODIGODEBARRAS(CHR(205)
        || CHR(102)
        || '415'
        || 's$codigoEan$s'
        || '8020'
        || LPAD(NVL(VPF.TIPO_COMPROBANTE,'0'),5,0)
        || LPAD(VF.NUMERO_FACTURA,19,0)
        || CHR(102)
        || '3900'
        || LPAD(VF.VALOR,14,0)
        || CHR(102)
        || '96'
        || TO_CHAR(SYSDATE + 5,'YYYYMMDD')))  CODIGODEBARRAS_BIN

FROM VP_PROCESO_FACTURACION VPF
INNER JOIN VP_FACTURA VF
ON VPF.ID = VF.ID_PROCESO_FACTURACION
INNER JOIN VP_DETALLE_FACTURA VDF
ON VF.ID = VDF.ID_FACTURA
INNER JOIN VP_PROYECTOS VP
ON VPF.ID_PROYECTO = VP.ID
INNER JOIN VP_BENEFICIARIOS VB
ON VF.ID_BENEFICIARIOS = VB.ID
INNER JOIN IP_USUARIOS_PREDIAL IUP
ON VB.COMPANIA = IUP.COMPANIA
AND VB.IP_CODIGO = IUP.CODIGO
AND VB.IP_NUMERO_ORDEN = IUP.NUMERO_ORDEN
LEFT JOIN VP_FACTORES VP_DES
ON VB.DESTINACION_ECONOMICA = VP_DES.CODIGO
LEFT JOIN VP_FACTORES VP_BEN
ON VB.GRADO_BENEFICIO = VP_BEN.CODIGO
WHERE VPF.COMPANIA = s$compania$s
AND VP.ID = s$idProyecto$s
AND VPF.NUMERO = s$numeroProceso$s
AND VB.ID = CASE WHEN s$validar$s = 1 
THEN s$idBeneficiario$s 
ELSE VB.ID 
END
