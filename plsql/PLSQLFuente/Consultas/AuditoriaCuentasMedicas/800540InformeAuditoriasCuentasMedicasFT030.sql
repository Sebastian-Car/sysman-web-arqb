MERGE INTO CONSULTAS FIN USING (SELECT '800540InformeAuditoriasCuentasMedicasFT030' INFORME ,TO_CLOB(q'[SELECT '2' "TIPO DE REGISTRO",
           TO_CHAR(ROW_NUMBER() OVER (ORDER BY CM_AUDITORIA_GLOSAS.CONSECUTIVO_ARCHIVO)) AS "CONSECUTIVO",
           NVL(PCK_SYSMAN_UTL.FC_PAR(CM_IMPORTARRIPS.COMPANIA, 'TIPO DE IDENTIFICACION ENTIDADES ACM',84, SYSDATE), ' ') "TIPO IDENTIFICACION ERP", 
           COMPANIA.DEPARTAMENTO "DEPARTAMENTO",
           CM_ARCHIVO_TRANSACCIONES.NOMBRE_RAZON_SOCIAL "NOMBRE DE LA IPS Y/O EPS-S",
           CM_ARCHIVO_TRANSACCIONES.TIPO_IDENTI_PREST_SERV_SALUD "TIPO DE IDENTIFICACION IPS/EPSS",
           CM_ARCHIVO_TRANSACCIONES.NUM_IDENTIF_PRESTADOR "NUMERO DE IDENTIFICACION IPS/EPSS",
           'F' "TIPO COBRO",
           NVL(REGEXP_REPLACE(CM_AUDITORIA_GLOSAS.NUM_FACTURA,' *[0-9]*'),' ') "PREFIJO FACT",
           REGEXP_REPLACE(CM_AUDITORIA_GLOSAS.NUM_FACTURA,' *[A-Za-z]*') "NUMERO FACT",
           'I' "INDICADOR",
           CM_AUDITORIA_GLOSAS.VALOR_NETO_A_PAGAR_ENTI_CONTR "VALOR DE LA FACTURA",
           TO_CHAR(CM_ARCHIVO_TRANSACCIONES.FECHA_EXP_FACTURA, 'DD/MM/YYYY') "FECHA EMISION DE FACTURA",
           TO_CHAR(CM_IMPORTARRIPS.FECHA, 'DD/MM/YYYY') "FECHA PRESENTACION DE FACTURA",
           ' ' "FECHA DEVOLUCION DE FACTURA",
           CM_AUDITORIA_GLOSAS.TOTAL_APROBADO_PAGAR "VALOR TOTAL PAGOS X FACTURA",
           CM_AUDITORIA_GLOSAS.VALOR_OBJECION "VALOR GLOSA ACEPTADA",
           'NO' "GLOSA RESPONDIDA",
           (CM_AUDITORIA_GLOSAS.VALOR_NETO_A_PAGAR_ENTI_CONTR - CM_AUDITORIA_GLOSAS.TOTAL_APROBADO_PAGAR - CM_AUDITORIA_GLOSAS.VALOR_OBJECION)  "SALDO FACTURA",
           'NO' "FACTURA EN COBRO JURIDICO",	
           '0' "ETAPA DEL PROCESO"
      FROM CM_IMPORTARRIPS
INNER JOIN COMPANIA ON CM_IMPORTARRIPS.COMPANIA = COMPANIA.CODIGO
INNER JOIN CM_ARCHIVO_TRANSACCIONES ON CM_IMPORTARRIPS.CONSECUTIVO = CM_ARCHIVO_TRANSACCIONES.CONSECUTIVO_RIPS
       AND CM_IMPORTARRIPS.COMPANIA = CM_ARCHIVO_TRANSACCIONES.COMPANIA
INNER JOIN CM_AUDITORIA_GLOSAS 
        ON CM_ARCHIVO_TRANSACCIONES.CONSECUTIVO_RIPS = CM_AUDITORIA_GLOSAS.CONSECUTIVO_ARCHIVO
       AND CM_ARCHIVO_TRANSACCIONES.COMPANIA = CM_AUDITORIA_GLOSAS.COMPANIA
       AND CM_ARCHIVO_TRANSACCIONES.COD_PREST_SERV_SALUD = CM_AUDITORIA_GLOSAS.COD_PREST_SERV_SALUD
       AND CM_ARCHIVO_TRANSACCIONES.NUM_FACTURA = CM_AUDITORIA_GLOSAS.NUM_FACTURA
     WHERE CM_IMPORTARRIPS.CLASECUENTA BETWEEN 's$ClaseCuentaInicial$s' AND 's$ClaseCuentaFinal$s'
       AND TO_CHAR(CM_IMPORTARRIPS.FECHA,'DD/MM/YYYY') BETWEEN TO_DATE('s$FechaInicial$s', 'DD/MM/YYYY') AND TO_DATE('s$FechaFinal$s', 'DD/MM/YYYY')]') CONSULTA, 84 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, 'ADMIN' CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);