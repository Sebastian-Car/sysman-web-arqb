MERGE INTO CONSULTAS FIN USING (SELECT '800704RelacionContractualTotal' INFORME ,TO_CLOB(q'[SELECT 
    NVL(TO_CHAR(ODC.NUMERO), 'ND') AS NUMERO,
    NVL(SUBSTR(ODC.OBJETOCONTRATO, 1, 2048), 'ND') AS OBJETOCONTRATO,
    NVL(TO_CHAR(ODC.FECHAFIRMA, 'YYYY/MM/DD'), 'ND') AS FECHA,
    NVL(ODC.VALORFINAL, 0) AS VALORFINAL,
    NVL(TA.DESCRIPCION, 'ND') AS MODALIDAD,
    NVL(ODC.CEDULACONTRATISTA, 'ND') AS CEDULACONTRATISTA,
    NVL(ODC.NOMBRECONTRATISTA, 'ND') AS NOMBRECONTRATISTA,
    NVL(SUBSTR(RES.RUBRONOMBRE, 1, 2048), 'ND') AS AFECTACION
FROM ORDENDECOMPRA ODC
LEFT JOIN (
SELECT COMPANIA, CLASEORDEN, NUMERO,
    LISTAGG(RUBRONOM, ',') WITHIN GROUP(
        ORDER BY
            COMPANIA, CLASEORDEN, NUMERO
    ) RUBRONOMBRE
FROM
    (
        SELECT DISTINCT
            ORDENDECOMPRAPPTO.COMPANIA,
            ORDENDECOMPRAPPTO.CLASEORDEN,
            ORDENDECOMPRAPPTO.NUMERO,
            CONCAT(ORDENDECOMPRAPPTO.RUBRO, CONCAT(' ', CONCAT(
                CASE
                    WHEN ORDENDECOMPRAPPTO.AUXILIAR = PCK_DATOS.FC_CONS_AUXILIAR THEN
                        ''
                    ELSE
                        ORDENDECOMPRAPPTO.AUXILIAR
                END, CONCAT(' ', PLAN_PRESUPUESTAL.NOMBRE)))) RUBRONOM
        FROM
            ORDENDECOMPRAPPTO
            INNER JOIN PLAN_PRESUPUESTAL ON ORDENDECOMPRAPPTO.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
                                            AND ORDENDECOMPRAPPTO.ANOPPTO = PLAN_PRESUPUESTAL.ANO
                                            AND ORDENDECOMPRAPPTO.RUBRO = PLAN_PRESUPUESTAL.CODIGO
            INNER JOIN TIPO_COMPROBPP ON ORDENDECOMPRAPPTO.COMPANIA = TIPO_COMPROBPP.COMPANIA
                                         AND ORDENDECOMPRAPPTO.TIPOPPTO = TIPO_COMPROBPP.CODIGO
        WHERE
            ORDENDECOMPRAPPTO.COMPANIA = s$compania$s
            AND TIPO_COMPROBPP.CLASE IN ('RES')
    ) UNICOS
GROUP BY
    COMPANIA,
    CLASEORDEN,
    NUMERO) RES
    ON RES.COMPANIA = ODC.COMPANIA 
    AND RES.CLASEORDEN = ODC.CLASEORDEN
    AND RES.NUMERO = ODC.NUMERO
LEFT JOIN TIPOADJUDICACION TA
ON ODC.COMPANIA = TA.COMPANIA 
AND ODC.TIPOADJUDICACION = TA.CODIGO
WHERE 
	    EXTRACT(YEAR FROM ODC.FECHAINICIO) = s$ano$s
            AND EXTRACT(MONTH FROM ODC.FECHAINICIO) BETWEEN s$mesInicial$s AND s$mesFinal$s
]') CONSULTA, 99 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);