SELECT  CODIGO_BPIN_SGR,
        FUENTES_FINANCIACION_SGR,
        SECTOR,
        NOMBRE_DEL_PROYECTO,
        SUM(APROPIACIONES) APROPIACIONES, 
        SUM(COMPROMISOS_SGR) COMPROMISOS_SGR, 
        SUM(OBLIGACIONES) OBLIGACIONES, 
        SUM(PAGOS)
FROM (s$consultabase$s)
GROUP BY CODIGO_BPIN_SGR,
        FUENTES_FINANCIACION_SGR,
        SECTOR,
        NOMBRE_DEL_PROYECTO
