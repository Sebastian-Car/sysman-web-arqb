MERGE INTO CONSULTAS FIN USING (SELECT '800484V_CGR_relacion_cdp' INFORME ,TO_CLOB(q'[SELECT TO_CHAR(dcp.ANO) Anio,
	CASE WHEN  pp.TIPOVIGENCIA ='RA'
          THEN 'Reservas'
          ELSE CASE WHEN pp.TIPOVIGENCIA ='RC'
          THEN 'CxP'
             ELSE 'Actual' END END Vigencia,
	SUBSTR(REPLACE(C.NITCOMPANIA, '.', ''),1,9) NIT_Entidad,
	c.NOMBRE  Nombre_Entidad,
    CASE WHEN ppc.CODIGO_CCEPT IS NULL
     THEN dcp.CUENTA 
     ELSE ppc.CODIGO_CCEPT END Cod_Cuenta,
	REPLACE(REPLACE(CASE WHEN cfr.CODIGO IS NULL
       THEN fr.NOMBRE
       ELSE cfr.NOMBRE END, CHR(13), ' ') , CHR(10), ' ') Fuente_Financiacion,
	pp.NOMBRE Nombre_Cuenta,
    'DA' Macro_campo_nivel_agregado,
	dcp.TIPO_CPTE || '-'||NVL(TO_CHAR(dcp.COMPROBANTE ),'N/A') Numero_CDP,
	NVL(TO_CHAR(dcp.FECHA,'YYYY-MM-DD'),'1900-01-01')  Fecha_CDP,
	REGEXP_REPLACE(REGEXP_REPLACE(REPLACE(NVL(dcp.VALOR_DEBITO,0) - NVL(dcp.VALOR_CREDITO,0),'.',','),'(^-,)','-0,'),'(^,)','0,') Valor_CDP,
	REPLACE(REPLACE(NVL(dcp.DESCRIPCION,'N/A'), CHR(13), ' ') , CHR(10), ' ') Detalle_CDP
FROM
	COMPROBANTE_PPTAL cp
INNER JOIN DETALLE_COMPROBANTE_PPTAL dcp 
ON 	cp.COMPANIA = dcp.COMPANIA
	AND cp.ANO = dcp.ANO
	AND cp.TIPO = dcp.TIPO_CPTE
	AND cp.NUMERO = dcp.COMPROBANTE
INNER JOIN COMPANIA c 
    ON 	cp.COMPANIA = c.CODIGO
INNER JOIN PLAN_PRESUPUESTAL pp 
    ON 	dcp.COMPANIA = pp.COMPANIA
	AND dcp.ANO = pp.ANO
	AND dcp.CUENTA = pp.CODIGO
 INNER JOIN PLAN_PPTAL_CONFIG ppc
    ON dcp.COMPANIA=ppc.COMPANIA
    AND  dcp.ANO=ppc.ANO
    AND  dcp.CUENTA = ppc.CODIGO
    AND  CASE WHEN PP.MAN_AUX_GEN=-1 THEN dcp.AUXILIAR ELSE '99999999999999999999' END = ppc.AUXILIAR 
    AND  CASE WHEN PP.MAN_CEN_CTO=-1 THEN dcp.CENTRO_COSTO ELSE '99999999999999999999' END =ppc.CENTRO_COSTO 
    AND  CASE WHEN PP.MAN_AUX_FUE=-1 THEN dcp.FUENTE_RECURSO  ELSE '99999999999999999999' END= ppc.FUENTE_RECURSO 
    AND  CASE WHEN PP.MAN_AUX_REF=-1 THEN dcp.REFERENCIA ELSE '99999999999999999999' END= ppc.REFERENCIA 
LEFT JOIN FUENTE_RECURSOS fr
    ON 	dcp.COMPANIA = fr.COMPANIA
	AND dcp.ANO = fr.ANO
	AND CASE WHEN pp.MAN_AUX_FUE =-1 THEN dcp.FUENTE_RECURSO ELSE pp.FUENTE_RECURSOS END  = fr.CODIGO
INNER JOIN TIPO_COMPROBPP tc 
   ON 	cp.COMPANIA = tc.COMPANIA
	AND cp.TIPO = tc.CODIGO
LEFT JOIN CUIPO_CODIGO_CCEPT ccc
  ON ppc.COMPANIA= ccc.COMPANIA
  AND ppc.ANO= ccc.ANO
  AND ppc.CODIGO_CCEPT= ccc.CODIGO
  LEFT JOIN CUIPO_FUENTES cfr
   ON ppc.COMPANIA= cfr.COMPANIA
  AND ppc.ANO= cfr.ANO
  AND ppc.FUENTE_CUIPO= cfr.CODIGO   
WHERE
	cp.COMPANIA = s$compania$s
    and cp.ANO=s$ano$s 
	AND cp.MES  BETWEEN s$mesInicial$s AND s$mesFinal$s
	AND tc.CLASE IN ('ADD', 'DIS', 'DMD')
   ORDER BY  dcp.FECHA]') CONSULTA, 99 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
