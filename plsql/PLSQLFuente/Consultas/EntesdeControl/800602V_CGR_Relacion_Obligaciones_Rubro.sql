	CASE WHEN  pp.TIPOVIGENCIA ='RA'
          THEN 'Reservas'
          ELSE CASE WHEN pp.TIPOVIGENCIA ='RC'
          THEN 'CxP'
             ELSE 'Actual' END END Vigencia,
	SUBSTR(REPLACE(C.NITCOMPANIA, '.', ''),1,9)  NIT_Entidad_reportante,
	c.NOMBRE  Nombre_Entidad,
	dcp.CUENTA Codigo_Presupuestal,
	'DA' Macro_campo_nivel_agregado,
	NVL(CODIGO_CCPET.NOMBRE,'N/A')  Nombre_Rubro,
	dcp.TIPO_CPTE || '-'||NVL(TO_CHAR(dcp.COMPROBANTE),0) numero_de_obligacion,
	NVL(TO_CHAR(dcp.FECHA,'YYYY-MM-DD'),'1900-01-01') Fecha_de_Obligacion,
	dcp.TIPO_CPTE_AFECT || '-'||NVL(TO_CHAR(dcp.CMPTE_AFECTADO),'N/A')  Numero_Compromiso,
	NVL(TO_CHAR(CP.FECHA,'YYYY-MM-DD'),'1900-01-01')  Fecha_Compromiso,
	REGEXP_REPLACE(REGEXP_REPLACE(REPLACE(NVL(dcp.VALOR_DEBITO,0) - NVL(VALOR_CREDITO,0),'.',','),'(^-,)','-0,'),'(^,)','0,')  Valor_obligacion,
	NVL(t.NIT,0) identificacion_Beneficiario,
	NVL(t.NOMBRE,'N/A')  Nombre_Beneficiario,
	REPLACE(REPLACE(NVL(dcp.DESCRIPCION,'N/A'), CHR(13), ' ') , CHR(10), ' ') Detalle_compromiso
