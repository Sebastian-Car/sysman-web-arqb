SELECT CODIGOSFUT.CODIGOFUT AS CODIGO, 
       CASE WHEN TERCERO.CLASEENTIDADOFICIAL NOT IN(0) 
            THEN NVL(TERCERO.NIT_CEDULA, NIT)
            ELSE '0'
            END  NOMBRE, 
       SUM(CASE WHEN PLAN.NATURALEZA ='D' 
                THEN VALOR_DEBITO  - VALOR_CREDITO
                ELSE VALOR_CREDITO - VALOR_DEBITO
                END) PRESUPUESTO_DEFINITIVO
FROM V_PLAN_PRESUPUESTAL PLAN INNER JOIN DETALLE_COMPROBANTE_PPTAL DET
     ON PLAN.COMPANIA = DET.COMPANIA
    AND PLAN.ANO      = DET.ANO
    AND PLAN.ID       = DET.ID 
  INNER JOIN TIPO_COMPROBPP
     ON DET.COMPANIA  = TIPO_COMPROBPP.COMPANIA 
    AND DET.TIPO_CPTE = TIPO_COMPROBPP.CODIGO 
  INNER JOIN  TERCERO 
     ON TERCERO.COMPANIA = DET.COMPANIA 
    AND TERCERO.SUCURSAL = DET.SUCURSAL 
    AND TERCERO.NIT      = DET.TERCERO 
  INNER JOIN CODIGOSFUT 
    ON PLAN.COMPANIA    = CODIGOSFUT.COMPANIA
   AND PLAN.ANO         = CODIGOSFUT.ANO
   AND PLAN.CODIGOFUT_H = CODIGOSFUT.CODIGOFUT 
WHERE PLAN.COMPANIA = s$compania$s
  AND PLAN.ANO      = s$anioTrabajo$s 
  AND PLAN.FUT_TRANSFERENCIA NOT IN(0)
  AND DET.MES              <= s$mesfinal$s
  AND TIPO_COMPROBPP.CLASE In ('RES','DMR','ADR') 
  AND PLAN.NATURALEZA = 'D'
  AND PLAN.DESTINO='F'
GROUP BY CODIGOSFUT.CODIGOFUT, 
        CASE WHEN TERCERO.CLASEENTIDADOFICIAL NOT IN(0) 
            THEN NVL(TERCERO.NIT_CEDULA, NIT)
            ELSE '0'
            END
