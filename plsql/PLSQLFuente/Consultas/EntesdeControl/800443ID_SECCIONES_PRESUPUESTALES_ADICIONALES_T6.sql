MERGE INTO CONSULTAS FIN USING (SELECT '800443ID_SECCIONES_PRESUPUESTALES_ADICIONALES_T6' INFORME ,TO_CLOB(q'[SELECT CODIGO, 
s$nombre$s
SECCION_PPTAL,
NOMBRE_SECCION
FROM(
SELECT 
       PLAN_PRESUPUESTAL.ANO,
       '2.99' CODIGO,
       PLAN_PRESUPUESTAL.NOMBRE,
       TIPOCLASIFICADOR.CODIGO SECCION_PPTAL,
       TIPOCLASIFICADOR.NOMBRE NOMBRE_SECCION
  FROM PLAN_PRESUPUESTAL
INNER JOIN TIPOCLASIFICADOR 
        ON PLAN_PRESUPUESTAL.COMPANIA        = TIPOCLASIFICADOR.COMPANIA
       AND PLAN_PRESUPUESTAL.ANO             = TIPOCLASIFICADOR.ANO
       AND PLAN_PRESUPUESTAL.CODIGOUNIDADEJE = TIPOCLASIFICADOR.CODIGO
     WHERE PLAN_PRESUPUESTAL.COMPANIA = s$compania$s
       AND PLAN_PRESUPUESTAL.ANO = s$anio$s
       AND TIPOCLASIFICADOR.CLASECLASIFICADOR = '008'
       AND TIPOCLASIFICADOR.SECCIONESADI NOT IN (0)
       AND TIPOCLASIFICADOR.MOVIMIENTO NOT IN (0)) CUIPO
GROUP BY  CODIGO, 
s$nombre$s
SECCION_PPTAL,
NOMBRE_SECCION
ORDER BY CODIGO]') CONSULTA, 99 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);