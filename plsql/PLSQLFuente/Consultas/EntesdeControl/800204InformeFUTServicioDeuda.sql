SELECT 'VAL' CODIGOFUT_H,
        PCK_SYSMAN_UTL.FC_PAR(V_PLAN_PRESUPUESTAL.COMPANIA,'TIPO OPERACION NO APLICA EN SERVICIO A LA DEUDA',-1, SYSDATE) TIPODEUDA_FUT,
       PCK_SYSMAN_UTL.FC_PAR(V_PLAN_PRESUPUESTAL.COMPANIA,'TIPO DEUDA NO APLICA EN SERVICIO A LA DEUDA',-1,SYSDATE) TIPOOPERACION_FUT,
       PCK_SYSMAN_UTL.FC_PAR(V_PLAN_PRESUPUESTAL.COMPANIA,'FUENTE NO APLICA EN SERVICIO A LA DEUDA',-1,SYSDATE) FUENTE,
      SUM(V_RESUMENPPTO_BASE.APROPIADO) PRESUPUESTO_INICIAL,
      SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE) PRESUPUESTO_DEFINITIVO,
      SUM(V_RESUMENPPTO_BASE.COMPROMISOSACUM) COMPROMISOS,
      SUM(V_RESUMENPPTO_BASE.OBLIGACIONESACUM) OBLIGACIONES,
      SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT) PAGOS
  FROM V_PLAN_PRESUPUESTAL
  INNER JOIN  V_RESUMENPPTO_BASE 
        ON V_PLAN_PRESUPUESTAL.COMPANIA       =  V_RESUMENPPTO_BASE.COMPANIA 
       AND V_PLAN_PRESUPUESTAL.ANO            =  V_RESUMENPPTO_BASE.ANO
       AND V_PLAN_PRESUPUESTAL.ID             =  V_RESUMENPPTO_BASE.ID
  WHERE V_PLAN_PRESUPUESTAL.COMPANIA    = s$compania$s
    AND V_RESUMENPPTO_BASE.ANO          = s$anioTrabajo$s
    AND V_RESUMENPPTO_BASE.MES         <= s$mesfinal$s
    AND V_PLAN_PRESUPUESTAL.DESTINO     = 'S'
    AND V_RESUMENPPTO_BASE.NATURALEZA   ='D'
    AND V_PLAN_PRESUPUESTAL.TIPOVIGENCIA='VA'  
    AND V_PLAN_PRESUPUESTAL.MOVIMIENTO NOT IN(0) 
    GROUP BY 'VAL', 
     PCK_SYSMAN_UTL.FC_PAR(V_PLAN_PRESUPUESTAL.COMPANIA,'TIPO OPERACION NO APLICA EN SERVICIO A LA DEUDA',-1, SYSDATE),
       PCK_SYSMAN_UTL.FC_PAR(V_PLAN_PRESUPUESTAL.COMPANIA,'TIPO DEUDA NO APLICA EN SERVICIO A LA DEUDA',-1,SYSDATE),
       PCK_SYSMAN_UTL.FC_PAR(V_PLAN_PRESUPUESTAL.COMPANIA,'FUENTE NO APLICA EN SERVICIO A LA DEUDA',-1,SYSDATE) 
  HAVING SUM(V_RESUMENPPTO_BASE.APROPIADO +
    V_RESUMENPPTO_BASE.APROPIACIONVIGENTE +
    V_RESUMENPPTO_BASE.COMPROMISOSACUM +
    V_RESUMENPPTO_BASE.OBLIGACIONESACUM +
    V_RESUMENPPTO_BASE.EJECUCIONPPT) <> 0
UNION ALL       
SELECT * FROM
(
SELECT 
    V_PLAN_PRESUPUESTAL.CODIGOFUT_H,
    V_PLAN_PRESUPUESTAL.TIPODEUDA_FUT,
    V_PLAN_PRESUPUESTAL.TIPOOPERACION_FUT,
    tO_CHAR(V_PLAN_PRESUPUESTAL.FUENTE_FUT) AS FUENTE,
    SUM(V_RESUMENPPTO_BASE.APROPIADO) PRESUPUESTO_INICIAL,
    SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE) PRESUPUESTO_DEFINITIVO,
    SUM(V_RESUMENPPTO_BASE.COMPROMISOSACUM) COMPROMISOS,
    SUM(V_RESUMENPPTO_BASE.OBLIGACIONESACUM) OBLIGACIONES,
    SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT) PAGOS
  FROM V_PLAN_PRESUPUESTAL
  INNER JOIN  V_RESUMENPPTO_BASE 
        ON V_PLAN_PRESUPUESTAL.COMPANIA       =  V_RESUMENPPTO_BASE.COMPANIA 
       AND V_PLAN_PRESUPUESTAL.ANO            =  V_RESUMENPPTO_BASE.ANO
       AND V_PLAN_PRESUPUESTAL.ID             =  V_RESUMENPPTO_BASE.ID
  WHERE V_PLAN_PRESUPUESTAL.COMPANIA    = s$compania$s
    AND V_RESUMENPPTO_BASE.ANO          = s$anioTrabajo$s
    AND V_RESUMENPPTO_BASE.MES         <= s$mesfinal$s
    AND V_PLAN_PRESUPUESTAL.DESTINO     = 'S'
    AND V_RESUMENPPTO_BASE.NATURALEZA   ='D'
    AND V_PLAN_PRESUPUESTAL.TIPOVIGENCIA='VA' 
AND V_PLAN_PRESUPUESTAL.MOVIMIENTO NOT IN(0)  
  GROUP BY  
    V_PLAN_PRESUPUESTAL.CODIGOFUT_H,
     V_PLAN_PRESUPUESTAL.FUENTE_FUT,
      V_PLAN_PRESUPUESTAL.TIPODEUDA_FUT,
           V_PLAN_PRESUPUESTAL.TIPOOPERACION_FUT
  HAVING SUM(V_RESUMENPPTO_BASE.APROPIADO +
    V_RESUMENPPTO_BASE.APROPIACIONVIGENTE +
    V_RESUMENPPTO_BASE.COMPROMISOSACUM +
    V_RESUMENPPTO_BASE.OBLIGACIONESACUM +
    V_RESUMENPPTO_BASE.EJECUCIONPPT) <> 0
  
 ORDER BY   V_PLAN_PRESUPUESTAL.CODIGOFUT_H,
            V_PLAN_PRESUPUESTAL.FUENTE_FUT
            ) t