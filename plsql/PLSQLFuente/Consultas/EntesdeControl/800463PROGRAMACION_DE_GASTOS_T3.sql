MERGE INTO CONSULTAS FIN USING (SELECT '800463PROGRAMACION_DE_GASTOS_T3' INFORME ,TO_CLOB(q'[SELECT CASE WHEN SUBSTR(CODIGO,1,3)='2.3'
       THEN '2.99'
       ELSE CODIGO
       END CODIGO,
       s$nombre$s
VIGENCIA_GASTO,
SECCION_PRESUPUESTAL,
--SECTOR,
NVL(PROGRAMA,0) PROGRAMA,
NVL(BPIN,0)BPIN ,
SUM(APROPIACION_INICIAL)APROPIACION_INICIAL,
SUM(APROPIACION_DEFINITIVA) APROPIACION_DEFINITIVA
FROM(
SELECT CUIPO_CODIGO_CCEPT.CODIGO,
       CUIPO_CODIGO_CCEPT.NOMBRE,
       PLAN_PRESUPUESTAL.CODIGOUNIDADEJE SECCION_PRESUPUESTAL,
       CASE WHEN PLAN_PRESUPUESTAL.TIPOVIGENCIA = 'RA' THEN
               '2.0'
           ELSE CASE WHEN PLAN_PRESUPUESTAL.TIPOVIGENCIA = 'RC' THEN 
                '3.0'
             ELSE '1.0' END
       END  VIGENCIA_GASTO,
       PLAN_PRESUPUESTAL.SECTOR "SECTOR",
       CUIPO_PROGRAMA.CODIGO_EQUIVALENTE "PROGRAMA",
       PLAN_PRESUPUESTAL.CODIGOBPIN "BPIN",
       SUM(SAL.APROPIACION_DEBITO) APROPIACION_INICIAL,
       SUM(SAL.APROPIACION_DEBITO + SAL.ADICION + SAL.REDUCCION +(SAL.TRASLADO_DEBITO - SAL.TRASLADO_CREDITO)+(SAL.APLAZAM_DEBITO
       - SAL.APLAZAM_CREDITO))"APROPIACION_DEFINITIVA"
      FROM CUIPO_CODIGO_CCEPT
INNER JOIN PLAN_PRESUPUESTAL
        ON CUIPO_CODIGO_CCEPT.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
       AND CUIPO_CODIGO_CCEPT.CODIGO = PLAN_PRESUPUESTAL.CODIGOCCPET
       AND CUIPO_CODIGO_CCEPT.ANO = PLAN_PRESUPUESTAL.ANO      
INNER JOIN SALDO_AUX_PPTAL SAL 
        ON PLAN_PRESUPUESTAL.COMPANIA=SAL.COMPANIA
       AND PLAN_PRESUPUESTAL.ANO = SAL.ANO
       AND PLAN_PRESUPUESTAL.CODIGO = SAL.CODIGO
 LEFT JOIN CUIPO_PROGRAMA 
        ON CUIPO_PROGRAMA.COMPANIA=PLAN_PRESUPUESTAL.COMPANIA
       AND CUIPO_PROGRAMA.CODIGO= PLAN_PRESUPUESTAL.PROGRAMA
     WHERE CUIPO_CODIGO_CCEPT.COMPANIA = s$compania$s
       AND PLAN_PRESUPUESTAL.ANO = s$anio$s
       AND SAL.MES  <= s$mesfinal$s
       AND PLAN_PRESUPUESTAL.NATURALEZA = 'D'
       AND PLAN_PRESUPUESTAL.REGALIAS IN(0)
  GROUP BY CUIPO_CODIGO_CCEPT.CODIGO,
           CUIPO_CODIGO_CCEPT.NOMBRE,
           PLAN_PRESUPUESTAL.CODIGOUNIDADEJE,
           CASE WHEN PLAN_PRESUPUESTAL.TIPOVIGENCIA = 'RA' THEN '2.0' ELSE 
                 CASE WHEN PLAN_PRESUPUESTAL.TIPOVIGENCIA = 'RC' THEN '3.0'
                     ELSE '1.0'
                 END
         END,
         PLAN_PRESUPUESTAL.SECTOR,
         CUIPO_PROGRAMA.CODIGO_EQUIVALENTE,
         PLAN_PRESUPUESTAL.CODIGOBPIN
   HAVING SUM(Sal.Apropiacion_DEBITO) <>0 
       OR SUM(Sal.Apropiacion_DEBITO + Sal.Adicion +Sal.Reduccion + (SAL.TRASLADO_DEBITO- SAL.TRASLADO_CREDITO) + (Sal.Aplazam_Debito -Sal.Aplazam_Credito)) <>0  
       )CUIPO
       GROUP BY CASE WHEN SUBSTR(CODIGO,1,3)='2.3'
       THEN '2.99'
       ELSE CODIGO
       END ,
       s$nombre$s
VIGENCIA_GASTO,
SECCION_PRESUPUESTAL,
--SECTOR,
NVL(PROGRAMA,0),
NVL(BPIN,0) 
ORDER BY CODIGO]') CONSULTA, 99 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);