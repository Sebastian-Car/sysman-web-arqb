SELECT     PLAN_PPTAL_CONFIG.FUT_DESPLAZADOS1 CONCEPTO 
          , PLAN_PPTAL_CONFIG.FUENTE_FUT  FUENTES_DE_FINANCIACION 
          , SUM(CASE WHEN PLAN_PPTAL_CONFIG.NATURALEZA='D'
                     THEN APROPIACION_DEBITO  - APROPIACION_CREDITO
                     ELSE APROPIACION_CREDITO - APROPIACION_DEBITO
                END)  PRESUPUESTO_INICIAL
          , SUM(CASE WHEN PLAN_PPTAL_CONFIG.NATURALEZA='D'
                     THEN APROPIACION_DEBITO  - APROPIACION_CREDITO
                     ELSE APROPIACION_CREDITO - APROPIACION_DEBITO
                END + SALDO_AUX_PPTAL.ADICION + SALDO_AUX_PPTAL.REDUCCION + 
                CASE WHEN PLAN_PPTAL_CONFIG.NATURALEZA='D'
                     THEN TRASLADO_DEBITO-TRASLADO_CREDITO
                     ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
                END) PRESUPUESTO_DEFINITIVO 
          , SUM(SALDO_AUX_PPTAL.REG_CONTRACT         + SALDO_AUX_PPTAL.MODIF_REG_CONT 
              + SALDO_AUX_PPTAL.REG_NO_CONTRACT      + SALDO_AUX_PPTAL.MODIF_REG_NOCONT) COMPROMISOS      
          , SUM(SALDO_AUX_PPTAL.REGISTRO_OBLIGACION + SALDO_AUX_PPTAL.MODIF_REGISTRO_OBLIGACION) OBLIGACIONES      
          , SUM(CASE WHEN PLAN_PPTAL_CONFIG.NATURALEZA='D'
                     THEN EJE_PPT_DEBITO  - EJE_PPT_CREDITO 
                     ELSE EJE_PPT_CREDITO - EJE_PPT_DEBITO 
                END ) PAGOS
  FROM PLAN_PPTAL_CONFIG
  INNER JOIN SALDO_AUX_PPTAL  
    ON PLAN_PPTAL_CONFIG.COMPANIA       = SALDO_AUX_PPTAL.COMPANIA
   AND PLAN_PPTAL_CONFIG.ANO            = SALDO_AUX_PPTAL.ANO        
   AND PLAN_PPTAL_CONFIG.CODIGO         = SALDO_AUX_PPTAL.CODIGO
   AND PLAN_PPTAL_CONFIG.CENTRO_COSTO   = SALDO_AUX_PPTAL.CENTRO_COSTO
   AND PLAN_PPTAL_CONFIG.TERCERO        = SALDO_AUX_PPTAL.TERCERO
   AND PLAN_PPTAL_CONFIG.SUCURSAL       = SALDO_AUX_PPTAL.SUCURSAL
   AND PLAN_PPTAL_CONFIG.AUXILIAR       = SALDO_AUX_PPTAL.AUXILIAR
   AND PLAN_PPTAL_CONFIG.REFERENCIA     = SALDO_AUX_PPTAL.REFERENCIA    
   AND PLAN_PPTAL_CONFIG.FUENTE_RECURSO = SALDO_AUX_PPTAL.FUENTE_RECURSO
WHERE PLAN_PPTAL_CONFIG.COMPANIA = s$compania$s
   AND PLAN_PPTAL_CONFIG.ANO     = s$anioTrabajo$s
   AND SALDO_AUX_PPTAL.MES      <= s$mesfinal$s
   AND PLAN_PPTAL_CONFIG.FUENTE_FUT       IS NOT NULL 
   AND PLAN_PPTAL_CONFIG.FUT_DESPLAZADOS1 IS NOT NULL
 GROUP BY   SALDO_AUX_PPTAL.COMPANIA 
          , SALDO_AUX_PPTAL.ANO 
          , PLAN_PPTAL_CONFIG.NATURALEZA 
          , PLAN_PPTAL_CONFIG.FUENTE_FUT
          , PLAN_PPTAL_CONFIG.FUT_DESPLAZADOS1
