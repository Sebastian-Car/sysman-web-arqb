WITH SALDOSACUM AS (
SELECT 
   COMPANIA
  ,ANO
  ,MES
  ,ID     
  ,CODIGO
  ,SUM(APROPIACIONVIGENTE)      APROPIACIONVIGENTE
  ,SUM(INGRESOSCAUSADOS)        INGRESOSCAUSADOSACUM
  ,SUM(MODIFICACIONICA)         MODIFINGRESOSCAUSADOSACUM
  ,SUM(INGRESOS_EFECTIVO)       INGRESOS_EFECTIVOACUM
  ,SUM(MODIF_INGRESOS_EFECTIVO) MODIFINGRESOSEFECTIVOACUM
  ,SUM(INGRESOS_PAPELES)        INGRESOS_PAPELESACUM
  ,SUM(MODIF_INGRESOS_PAPELES)  MODIFINGRESOSPAPELESACUM
FROM V_RESUMENPPTO_BASE
WHERE COMPANIA = s$compania$s
  AND ANO      = s$anio$s
GROUP BY 
   COMPANIA
  ,ANO
  ,MES
  ,ID
  ,CODIGO
  ,NATURALEZA
  ,CENTRO_COSTO
  ,TERCERO
  ,SUCURSAL
  ,AUXILIAR
  ,REFERENCIA
  ,FUENTE_RECURSO
  ,MOVIMIENTO
)
SELECT
   DETALLE.DIA
  ,DETALLE.MES
  ,DETALLE.ANO ANIO
  ,DETALLE.COMPROBANTE
  ,DETALLE.TIPO_CPTE
  ,TC.CLASE
  ,(DETALLE.VALOR_CREDITO - DETALLE.VALOR_DEBITO) VALORTOTAL
  ,CASE WHEN (DETALLE.VALOR_CREDITO - DETALLE.VALOR_DEBITO) < 0 
        THEN '-'
        ELSE '+'
   END SIGNO
  ,ABS(DETALLE.VALOR_CREDITO - DETALLE.VALOR_DEBITO) VALOR
  ,CASE WHEN TC.CLASE IN('ICA')
        THEN 'DC'
        WHEN TC.CLASE IN('MIC','DIC','AIC')
        THEN 'DA'
        WHEN TC.CLASE IN('ING')             AND DETALLE.PAPELES     IN (0)
        THEN 'E'
        WHEN TC.CLASE IN('ING')             AND DETALLE.PAPELES NOT IN(0)
        THEN 'RP'
        WHEN TC.CLASE IN('MIN','DIN','AIN') AND DETALLE.PAPELES NOT IN (0)
        THEN 'OC'
        WHEN TC.CLASE IN('MIN','DIN','AIN') AND DETALLE.PAPELES     IN (0)
        THEN 'D'
   END CLASELETRAS
  ,CASE WHEN TC.CLASE IN('ICA') /*7*/
        THEN DETALLE.VALOR_CREDITO - 
             DETALLE.VALOR_DEBITO  +
             SALDOSACUM.INGRESOSCAUSADOSACUM
        ELSE 0
   END DERECHOSCAUSADOS
  ,CASE WHEN TC.CLASE IN('MIC','DIC','AIC') /*8*/
        THEN DETALLE.VALOR_CREDITO - 
             DETALLE.VALOR_DEBITO  +
             SALDOSACUM.MODIFINGRESOSCAUSADOSACUM
        ELSE 0
   END DERECHOSANULADOS
  ,CASE WHEN TC.CLASE IN('ING') AND DETALLE.PAPELES IN (0) /*10*/
        THEN DETALLE.VALOR_CREDITO - 
             DETALLE.VALOR_DEBITO  +
             SALDOSACUM.INGRESOS_EFECTIVOACUM
        ELSE 0
   END EFECTIVO
  ,CASE WHEN TC.CLASE IN('MIN','DIN','AIN') AND DETALLE.PAPELES IN (0) /*11*/
        THEN DETALLE.VALOR_CREDITO - 
             DETALLE.VALOR_DEBITO  +
             SALDOSACUM.MODIFINGRESOSEFECTIVOACUM             
        ELSE 0
   END DEVOLUCIONES
  ,CASE WHEN TC.CLASE IN('ING') AND DETALLE.PAPELES NOT IN(0) /*13*/
        THEN DETALLE.VALOR_CREDITO - 
             DETALLE.VALOR_DEBITO  +
             SALDOSACUM.INGRESOS_PAPELESACUM
        ELSE 0
   END RECAUDOENPAPELES
  ,CASE WHEN TC.CLASE IN('MIN','DIN','AIN') AND DETALLE.PAPELES NOT IN (0) /*15*/
        THEN DETALLE.VALOR_CREDITO - 
             DETALLE.VALOR_DEBITO  +
             SALDOSACUM.MODIFINGRESOSPAPELESACUM             
        ELSE 0
   END OTRASCANCELACIONES
  ,DETALLE.DESCRIPCION        
  ,PLAN.NOMBRE
  ,PLAN.NIVEL1
  ,PLAN.NIVEL2
  ,PLAN.NIVEL3
  ,PLAN.NIVEL4
  ,PLAN.NIVEL5
  ,PLAN.NIVEL6
  ,PLAN.NIVEL7
  ,PLAN.CODIGO
  ,PLAN.ID
  ,PLAN.NATURALEZA
  ,NVL(PLAN.ORIGENESPECIFICOINGRESOS,' ') ORIGENESPECIFICOINGRESOS
  ,NVL(SALDOSACUM.APROPIACIONVIGENTE,0) AUMENTO
  ,0 DISMINUCION
  ,DETALLE.FECHA
  ,0 EJECUCION
  ,0 REVERSION
  ,0 RECAUDOTOTAL
  ,0 UTILIZACION
  ,0 EMISION
  ,T.NOMBRE NOMBRETERCERO
  ,ES.NOMBRE NOMBREENTIDAD
  ,C.NOMBRE NOMBRECOMPANIA
FROM DETALLE_COMPROBANTE_PPTAL DETALLE
  INNER JOIN V_PLAN_PRESUPUESTAL PLAN
     ON DETALLE.COMPANIA = PLAN.COMPANIA
    AND DETALLE.ANO      = PLAN.ANO
    AND DETALLE.ID       = PLAN.ID
  INNER JOIN TIPO_COMPROBPP TC
     ON DETALLE.COMPANIA  = TC.COMPANIA
    AND DETALLE.TIPO_CPTE = TC.CODIGO
  INNER JOIN COMPANIA C
    ON PLAN.COMPANIA = C.CODIGO
  INNER JOIN ENTIDADES_SCHIP ES
    ON C.TIPOENTIDAD = ES.CODIGO
  INNER JOIN TERCERO T
     ON DETALLE.COMPANIA = T.COMPANIA
    AND DETALLE.TERCERO  = T.NIT
    AND DETALLE.SUCURSAL = T.SUCURSAL
  INNER JOIN SALDOSACUM
     ON PLAN.COMPANIA   = SALDOSACUM.COMPANIA
    AND PLAN.ANO        = SALDOSACUM.ANO
    AND PLAN.ID         = SALDOSACUM.ID   
    AND SALDOSACUM.MES <= DETALLE.MES-1
WHERE PLAN.COMPANIA = s$compania$s
  AND PLAN.ANO      = s$anio$s
  AND PLAN.NATURALEZA IN('C')
  AND EXTRACT(MONTH FROM DETALLE.FECHA) BETWEEN s$mesInicial$s AND s$mesFinal$s
  AND PLAN.CODIGO         BETWEEN s$cuentaIni$s AND s$cuentaFin$s
  AND LENGTH(PLAN.CODIGO) BETWEEN 0 AND s$nivel$s
  AND TC.CLASE IS NOT NULL
