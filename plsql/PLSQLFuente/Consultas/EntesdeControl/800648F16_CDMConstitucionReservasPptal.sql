MERGE INTO CONSULTAS FIN USING (SELECT '800648F16_CDMConstitucionReservasPptal' INFORME ,TO_CLOB(q'[SELECT 
  TO_CHAR(DETALLE_COMPROBANTE_PPTAL.CUENTA) AS "Codigo Presupuestal",
  TO_CHAR(DETALLE_COMPROBANTE_PPTAL.COMPROBANTE) AS "Numero De Registro",
  TO_CHAR(DETALLE_COMPROBANTE_PPTAL.FECHA,'DD/MM/YYYY') AS "Fecha Registro Presupuestal",
  (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO -DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) AS "Valor Registro Presupuestal",
  DETALLE_COMPROBANTE_PPTAL.TERCERO || ' - '
  || TERCERO.NOMBRE AS "Beneficiario",
    COMPROBANTE_PPTAL.TIPOCONTRATO
  || ' - '
  || COMPROBANTE_PPTAL.NUMEROCONTRATO AS "Contrato O Resolución",
  (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)-(DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO)+(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO) AS "Valor Reserva"
FROM PLAN_PRESUPUESTAL
INNER JOIN DETALLE_COMPROBANTE_PPTAL
ON PLAN_PRESUPUESTAL.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA
AND PLAN_PRESUPUESTAL.ANO     = DETALLE_COMPROBANTE_PPTAL.ANO
AND PLAN_PRESUPUESTAL.CODIGO  = DETALLE_COMPROBANTE_PPTAL.CUENTA
INNER JOIN TIPO_COMPROBPP
ON TIPO_COMPROBPP.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA
AND TIPO_COMPROBPP.CODIGO  = DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE
INNER JOIN TERCERO
ON DETALLE_COMPROBANTE_PPTAL.COMPANIA  = TERCERO.COMPANIA
AND DETALLE_COMPROBANTE_PPTAL.TERCERO  = TERCERO.NIT
AND DETALLE_COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL
INNER JOIN COMPROBANTE_PPTAL
ON DETALLE_COMPROBANTE_PPTAL.COMPANIA     = COMPROBANTE_PPTAL.COMPANIA
AND DETALLE_COMPROBANTE_PPTAL.ANO         = COMPROBANTE_PPTAL.ANO
AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = COMPROBANTE_PPTAL.TIPO
AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO
INNER JOIN TIPOVIGENCIA 
ON PLAN_PRESUPUESTAL.TIPOVIGENCIA = TIPOVIGENCIA.CODIGO
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA  = s$compania$s
AND TIPO_COMPROBPP.CLASE                  = 'RES'
AND TRUNC(DETALLE_COMPROBANTE_PPTAL.FECHA) BETWEEN  TO_DATE('01/'|| s$mesInicial$s ||'/'|| (s$ano$s - 1),'DD/MM/YYYY') AND LAST_DAY(TO_DATE('01/'|| s$mesFinal$s ||'/'|| (s$ano$s -1),'DD/MM/YYYY'))
AND ROUND((DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)-(DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO)+(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO),2)<>0
AND TIPOVIGENCIA.REPORTAR_SIA NOT IN (0)
ORDER BY DETALLE_COMPROBANTE_PPTAL.COMPANIA,
  DETALLE_COMPROBANTE_PPTAL.ANO,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE]') CONSULTA, 99 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
  COMMIT;