WITH TMPREOEGR AS (
  SELECT  ' ' REGISTRO 
         ,0 NUMERORES 
         ,DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE 
         ,DETALLE_COMPROBANTE_PPTAL.COMPROBANTE NUMERO_REGISTRO
         ,DETALLE_COMPROBANTE_PPTAL.FECHA FECHA_RES 
         ,DETALLE_COMPROBANTE_PPTAL.CUENTA CODIGO_CUENTA
         ,DETALLE_COMPROBANTE_PPTAL_1.CONSECUTIVO CONSECUTIVOAFECT 
         ,DETALLE_COMPROBANTE_PPTAL.CONSECUTIVO 
         ,TERCERO.NIT NIT_TERCERO 
         ,TERCERO.NOMBRE TERCERO 
         ,(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO 
           - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO 
           + DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO 
           - DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO) VALOR_RES 
         ,DETALLE_COMPROBANTE_PPTAL.DESCRIPCION 
         ,DETALLE_COMPROBANTE_PPTAL_1.TIPO_CPTE TIPOAFECT 
         ,DETALLE_COMPROBANTE_PPTAL_1.COMPROBANTE REO 
         ,(DETALLE_COMPROBANTE_PPTAL_1.VALOR_DEBITO 
           - DETALLE_COMPROBANTE_PPTAL_1.VALOR_CREDITO 
           + DETALLE_COMPROBANTE_PPTAL_1.MODIFICACION_DEBITO 
           - DETALLE_COMPROBANTE_PPTAL_1.MODIFICACION_CREDITO) VALOR_PAGO 
         ,0 DISMINUCION_COM 
         ,DETALLE_COMPROBANTE_PPTAL_1.FECHA FECHA_REO 
         ,DETALLE_COMPROBANTE_PPTAL.COMPANIA 
         ,DETALLE_COMPROBANTE_PPTAL.ANO 
         ,DETALLE_COMPROBANTE_PPTAL_1.AUXILIAR RECURSO
         ,0 SALDORES
   FROM   DETALLE_COMPROBANTE_PPTAL
     LEFT JOIN TERCERO
       ON DETALLE_COMPROBANTE_PPTAL.COMPANIA  = TERCERO.COMPANIA
      AND DETALLE_COMPROBANTE_PPTAL.TERCERO  = TERCERO.NIT
      AND DETALLE_COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL
     LEFT JOIN TIPO_COMPROBPP
       ON DETALLE_COMPROBANTE_PPTAL.COMPANIA   = TIPO_COMPROBPP.COMPANIA
      AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
     LEFT JOIN DETALLE_COMPROBANTE_PPTAL DETALLE_COMPROBANTE_PPTAL_1
       ON DETALLE_COMPROBANTE_PPTAL.COMPANIA     = DETALLE_COMPROBANTE_PPTAL_1.COMPANIA
      AND DETALLE_COMPROBANTE_PPTAL.ANO         = DETALLE_COMPROBANTE_PPTAL_1.ANO
      AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = DETALLE_COMPROBANTE_PPTAL_1.TIPO_CPTE_AFECT
      AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = DETALLE_COMPROBANTE_PPTAL_1.CMPTE_AFECTADO
      AND DETALLE_COMPROBANTE_PPTAL.CONSECUTIVO = DETALLE_COMPROBANTE_PPTAL_1.CONSECUTIVOPPTO
     LEFT JOIN TIPO_COMPROBPP TIPO_COMPROBPP_1
       ON DETALLE_COMPROBANTE_PPTAL_1.COMPANIA   = TIPO_COMPROBPP_1.COMPANIA
      AND DETALLE_COMPROBANTE_PPTAL_1.TIPO_CPTE = TIPO_COMPROBPP_1.CODIGO
   WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA = s$compania$s
     AND DETALLE_COMPROBANTE_PPTAL.ANO      = s$anio$s
     AND DETALLE_COMPROBANTE_PPTAL.FECHA BETWEEN s$fechainicial$s AND s$fechafinal$s
     AND (TIPO_COMPROBPP_1.CLASE            = 'REO'
      OR TIPO_COMPROBPP_1.CLASE             IS NULL)
     AND TIPO_COMPROBPP.CLASE               = 'RES'
), TMPREOEGR2 AS (
  SELECT  TMPREOEGR.TIPO_CPTE REGISTRO
         ,TMPREOEGR.NUMERO_REGISTRO NUMERORES
         ,TMPREOEGR.COMPANIA
         ,TMPREOEGR.ANO
         ,TMPREOEGR.CONSECUTIVOAFECT CONSECUTIVO
         ,DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE TIPOAFECT
         ,DETALLE_COMPROBANTE_PPTAL.COMPROBANTE REO
         ,TMPREOEGR.CODIGO_CUENTA
         ,TMPREOEGR.TIPOAFECT TIPO_CPTE
         ,TMPREOEGR.REO NUMERO_REGISTRO
         ,DETALLE_COMPROBANTE_PPTAL.CONSECUTIVO CONSECUTIVOAFECT
         ,DETALLE_COMPROBANTE_PPTAL.FECHA FECHA_RES
         ,VALOR_DEBITO -VALOR_CREDITO VALOR_RES
         ,VALOR_DEBITO -VALOR_CREDITO VALOR_PAGO
         ,CASE
            WHEN DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE='DRO'
            THEN ABS(VALOR_DEBITO-VALOR_CREDITO)
            ELSE 0
          END DISMINUCION_COM
         ,TERCERO.NIT NIT_TERCERO
         ,TERCERO.NOMBRE TERCERO
         ,DETALLE_COMPROBANTE_PPTAL.AUXILIAR RECURSO
         ,DETALLE_COMPROBANTE_PPTAL.DESCRIPCION
         ,DETALLE_COMPROBANTE_PPTAL.FECHA FECHA_REO
         ,0 SALDORES
   FROM TERCERO
     INNER JOIN DETALLE_COMPROBANTE_PPTAL
        ON TERCERO.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA
       AND TERCERO.SUCURSAL = DETALLE_COMPROBANTE_PPTAL.SUCURSAL
       AND TERCERO.NIT      = DETALLE_COMPROBANTE_PPTAL.TERCERO
     INNER JOIN TMPREOEGR
        ON TMPREOEGR.COMPANIA         = DETALLE_COMPROBANTE_PPTAL.COMPANIA
       AND TMPREOEGR.ANO              = DETALLE_COMPROBANTE_PPTAL.ANO  
       AND TMPREOEGR.CONSECUTIVOAFECT = DETALLE_COMPROBANTE_PPTAL.CONSECUTIVOPPTO
       AND TMPREOEGR.REO              = DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO
       AND TMPREOEGR.TIPOAFECT        = DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT
     INNER JOIN TIPO_COMPROBPP
        ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA
       AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
     LEFT JOIN TMPREOEGR TMPREOEGR_1
       ON TMPREOEGR.COMPANIA         = TMPREOEGR_1.COMPANIA
      AND TMPREOEGR.ANO              = TMPREOEGR_1.ANO
      AND TMPREOEGR.TIPOAFECT        = TMPREOEGR_1.TIPO_CPTE
      AND TMPREOEGR.REO              = TMPREOEGR_1.NUMERO_REGISTRO
      AND TMPREOEGR.CONSECUTIVOAFECT = TMPREOEGR_1.CONSECUTIVO
   WHERE TMPREOEGR_1.COMPANIA IS NULL
), UNIONTMP AS (
  SELECT TIPO_CPTE REGISTRO,NUMERO_REGISTRO NUMERORES,TIPO_CPTE
        ,NUMERO_REGISTRO,TO_CHAR(FECHA_RES,'DD/MM/YYYY') FECHA_RES
        ,CODIGO_CUENTA,CONSECUTIVOAFECT,CONSECUTIVO,NIT_TERCERO
        ,TERCERO,VALOR_RES,DESCRIPCION,TIPOAFECT,REO,0 VALOR_PAGO
        ,DISMINUCION_COM,' ' FECHA_REO,COMPANIA,ANO,RECURSO,VALOR_RES SALDORES
  FROM   TMPREOEGR
UNION ALL
  SELECT REGISTRO,NUMERORES,TIPO_CPTE,NUMERO_REGISTRO
        ,TO_CHAR(FECHA_RES,'DD/MM/YYYY') FECHA_RES,CODIGO_CUENTA
        ,CONSECUTIVOAFECT,CONSECUTIVO,NIT_TERCERO,TERCERO,VALOR_RES
        ,DESCRIPCION,TIPOAFECT,REO
        ,CASE WHEN REGISTRO = TIPO_CPTE AND NUMERORES = NUMERO_REGISTRO 
              THEN 0 
              ELSE VALOR_PAGO END VALOR_PAGO
        ,DISMINUCION_COM
        ,CASE WHEN REGISTRO = TIPO_CPTE AND NUMERORES = NUMERO_REGISTRO 
              THEN ' ' 
              ELSE TO_CHAR(FECHA_REO,'DD/MM/YYYY') END FECHA_REO
        ,COMPANIA,ANO,RECURSO
        ,CASE WHEN REGISTRO = TIPO_CPTE AND NUMERORES = NUMERO_REGISTRO 
              THEN VALOR_RES 
              ELSE(CASE WHEN (LAG(SALDORES) OVER (ORDER BY NUMERORES)) IS NULL 
                        THEN 0 
                        ELSE (CASE WHEN (LAG(CODIGO_CUENTA) OVER (ORDER BY NUMERORES)) <> CODIGO_CUENTA 
                                     OR (LAG(NUMERORES) OVER (ORDER BY NUMERORES)) <> NUMERORES 
                                   THEN (LAG(SALDORES) OVER (ORDER BY NUMERORES))
                                   ELSE (CASE WHEN (LAG(SALDORES) OVER (ORDER BY NUMERORES)) = 0
                                              THEN 0
                                              ELSE (LAG(SALDORES) OVER (ORDER BY NUMERORES)) 
                                                   - ABS(NVL(VALOR_PAGO,0)) END) END) END) END SALDORES
  FROM   TMPREOEGR2)
SELECT DISTINCT UNIONTMP.REGISTRO
      ,TO_CHAR(UNIONTMP.NUMERORES) NUMERORES
      ,CASE WHEN UNIONTMP.REGISTRO = UNIONTMP.TIPO_CPTE
              AND UNIONTMP.NUMERORES = UNIONTMP.NUMERO_REGISTRO
            THEN UNIONTMP.FECHA_RES
            ELSE ' ' END "FECHA REGISTRO"
      ,UNIONTMP.NIT_TERCERO "NIT TERCERO"
      ,UNIONTMP.TERCERO
      ,UNIONTMP.CODIGO_CUENTA  "CODIGO CUENTA"
      ,UNIONTMP.RECURSO
      ,TO_CHAR(CASE WHEN UNIONTMP.REGISTRO = UNIONTMP.TIPO_CPTE
                      AND UNIONTMP.NUMERORES = UNIONTMP.NUMERO_REGISTRO
                    THEN UNIONTMP.VALOR_RES
                    ELSE 0 END) "VALOR REGISTRO"
      ,CASE WHEN UNIONTMP.REGISTRO = UNIONTMP.TIPO_CPTE
              AND UNIONTMP.NUMERORES = UNIONTMP.NUMERO_REGISTRO
            THEN ' '
            ELSE TO_CHAR(UNIONTMP.NUMERO_REGISTRO) END "NUMERO REO"
      ,CASE WHEN UNIONTMP.REGISTRO = UNIONTMP.TIPO_CPTE
              AND UNIONTMP.NUMERORES = UNIONTMP.NUMERO_REGISTRO
            THEN ' '
            ELSE UNIONTMP.FECHA_RES END "FECHA REO"
      ,CASE WHEN UNIONTMP.REGISTRO = UNIONTMP.TIPO_CPTE
              AND UNIONTMP.NUMERORES = UNIONTMP.NUMERO_REGISTRO
            THEN ' '
            ELSE TO_CHAR(UNIONTMP.REO) END EGRESO
      ,UNIONTMP.FECHA_REO "FECHA EGRESO"
      ,TO_CHAR(UNIONTMP.DISMINUCION_COM) "DISMINUCIONES COM" 
      ,TO_CHAR(CASE WHEN UNIONTMP.REGISTRO = UNIONTMP.TIPO_CPTE
                      AND UNIONTMP.NUMERORES = UNIONTMP.NUMERO_REGISTRO
                    THEN 0
                    ELSE UNIONTMP.VALOR_PAGO
                         + UNIONTMP.DISMINUCION_COM
               END) "VALOR PAGOS"
      ,UNIONTMP.DESCRIPCION
      ,TO_CHAR(UNIONTMP.SALDORES) SALDORES
FROM   UNIONTMP
ORDER BY UNIONTMP.REGISTRO
        ,UNIONTMP.NUMERORES
        ,UNIONTMP.CODIGO_CUENTA
        ,UNIONTMP.FECHA_REO
        ,CASE WHEN UNIONTMP.REGISTRO = UNIONTMP.TIPO_CPTE
              AND UNIONTMP.NUMERORES = UNIONTMP.NUMERO_REGISTRO
            THEN ' '
            ELSE TO_CHAR(UNIONTMP.REO)
         END
