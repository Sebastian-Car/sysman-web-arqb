MERGE INTO CONSULTAS FIN USING (SELECT '800595EjecucionPresupuestalIngresos' INFORME ,TO_CLOB(q'[SELECT
  PLAN_PRESUPUESTAL.CODIGO CUENTA,
  PLAN_PRESUPUESTAL.NOMBRE,
   SUM(SALDOS.APROPIADO) PRESUPUESTO_INICIAL,
   SUM(SALDOS.ADICION - SALDOS.REDUCCION) AS MODIFICACIONES_MES,
    SUM(SALDOS.MODIFICACIONES) MODIFICACIONES,
  SUM(SALDOS.APROPIADO + SALDOS.MODIFICACIONES) TOTALPRESUPUESTO,
  SUM(SALDOS.RECAUDOSMES)                          RECAUDOSMES,
  SUM(SALDOS.RECAUDOSTOTALES)                      RECAUDOSACUMULADOS,
  CASE WHEN SUM(SALDOS.APROPIADO + SALDOS.MODIFICACIONES)>0
       THEN SUM(SALDOS.RECAUDOSTOTALES)/SUM(SALDOS.APROPIADO + SALDOS.MODIFICACIONES) ELSE 0 END
       * 100
       AS PORCRECAUDADO,
  SUM(SALDOS.APROPIADO + SALDOS.MODIFICACIONES -SALDOS.RECAUDOSTOTALES) PORRECAUDAR,
  SUM(RECONOCIMIENTOS) AS RECONOCIMIENTO_VA,
 SUM(SALDOS.RECAUDOSTOTALES + RECONOCIMIENTOS)AS RECAUDO_RECONOCIMIENTO,
  'No Aplica' AS CODIGO_AREA,
  'No Aplica' AS NOMBRE_AREA,
  'No Aplica' AS CODIGO_SECTOR,
  'No Aplica' AS NOMBRE_SECTOR
FROM PLAN_PRESUPUESTAL INNER JOIN
(
SELECT
  SALDO_AUX_PPTAL.COMPANIA,
  SALDO_AUX_PPTAL.ANO,
  SALDO_AUX_PPTAL.CODIGO,
  SUM(CASE WHEN SALDO_AUX_PPTAL.NATURALEZA='D' AND  SALDO_AUX_PPTAL.MES < s$mesInicial$s
 THEN APROPIACION_DEBITO - APROPIACION_CREDITO ELSE APROPIACION_CREDITO - APROPIACION_DEBITO END)  APROPIADO,
  SUM(CASE WHEN SALDO_AUX_PPTAL.NATURALEZA='D' THEN APLAZAM_DEBITO     - APLAZAM_CREDITO     ELSE APLAZAM_CREDITO     - APLAZAM_DEBITO     END)  APLAZAMIENTO,
  SUM(CASE WHEN SALDO_AUX_PPTAL.NATURALEZA='D' THEN TRASLADO_DEBITO    - TRASLADO_CREDITO    ELSE TRASLADO_CREDITO    - TRASLADO_DEBITO    END)  TRASLADO,
  SUM(SALDO_AUX_PPTAL.ADICION)   ADICION,
  SUM(SALDO_AUX_PPTAL.REDUCCION) REDUCCION,
  SUM(SALDO_AUX_PPTAL.ADICION + SALDO_AUX_PPTAL.REDUCCION
      + CASE WHEN SALDO_AUX_PPTAL.NATURALEZA='D' THEN APLAZAM_DEBITO     - APLAZAM_CREDITO     ELSE APLAZAM_CREDITO     - APLAZAM_DEBITO     END
      + CASE WHEN SALDO_AUX_PPTAL.NATURALEZA='D' THEN TRASLADO_DEBITO    - TRASLADO_CREDITO    ELSE TRASLADO_CREDITO    - TRASLADO_DEBITO     END
  ) MODIFICACIONES,
  SUM(CASE WHEN SALDO_AUX_PPTAL.NATURALEZA='D' THEN EJE_PPT_DEBITO-EJE_PPT_CREDITO ELSE EJE_PPT_CREDITO-EJE_PPT_DEBITO END +
  SALDO_AUX_PPTAL.MODIF_INGRESOS_EFECTIVO +
  SALDO_AUX_PPTAL.MODIF_INGRESOS_PAPELES)  AS RECAUDOSTOTALES,
  SUM(CASE WHEN SALDO_AUX_PPTAL.MES  BETWEEN s$mesInicial$s AND s$mesFinal$s THEN
  (CASE WHEN SALDO_AUX_PPTAL.NATURALEZA='D' THEN EJE_PPT_DEBITO-EJE_PPT_CREDITO ELSE EJE_PPT_CREDITO-EJE_PPT_DEBITO END) +
  SALDO_AUX_PPTAL.MODIF_INGRESOS_EFECTIVO +
  SALDO_AUX_PPTAL.MODIF_INGRESOS_PAPELES  ELSE 0 END) AS RECAUDOSMES, 
  SUM(SALDO_AUX_PPTAL.RECONOCIMIENTOS)RECONOCIMIENTOS
FROM  SALDO_AUX_PPTAL
WHERE SALDO_AUX_PPTAL.COMPANIA = s$compania$s
AND SALDO_AUX_PPTAL.ANO        = s$ano$s
AND SALDO_AUX_PPTAL.MES  <= s$mesFinal$s
AND SALDO_AUX_PPTAL.NATURALEZA = 'C'
GROUP BY SALDO_AUX_PPTAL.COMPANIA, SALDO_AUX_PPTAL.ANO, SALDO_AUX_PPTAL.CODIGO, (SALDO_AUX_PPTAL.ADICION + SALDO_AUX_PPTAL.REDUCCION + CASE WHEN SALDO_AUX_PPTAL.NATURALE]') || TO_CLOB(q'[ZA='D' THEN APLAZAM_DEBITO - APLAZAM_CREDITO ELSE APLAZAM_CREDITO - APLAZAM_DEBITO END + CASE WHEN SALDO_AUX_PPTAL.NATURALEZA='D' THEN TRASLADO_DEBITO - TRASLADO_CREDITO ELSE TRASLADO_CREDITO - TRASLADO_DEBITO END )
) SALDOS
 ON PLAN_PRESUPUESTAL.COMPANIA = SALDOS.COMPANIA
AND PLAN_PRESUPUESTAL.ANO      = SALDOS.ANO
AND PLAN_PRESUPUESTAL.CODIGO      = SALDOS.CODIGO
GROUP BY PLAN_PRESUPUESTAL.COMPANIA, PLAN_PRESUPUESTAL.CODIGO , '0', PLAN_PRESUPUESTAL.NOMBRE,
 'No Aplica'
ORDER BY PLAN_PRESUPUESTAL.COMPANIA,
         PLAN_PRESUPUESTAL.CODIGO]') CONSULTA, 99 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);