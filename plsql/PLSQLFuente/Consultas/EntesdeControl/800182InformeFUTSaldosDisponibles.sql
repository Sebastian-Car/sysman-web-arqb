SELECT FUENTE_RECURSOS.CODIGO_FUT CONCEPTO,
         ROUND(SUM(CASE WHEN CUENTABANCOS.CLASECUENTA ='C' THEN SALDOs$mesfinal$s ELSE 0 END) , s$redondeo$s) CUENTAS_CORRIENTES,
         ROUND(SUM(CASE WHEN CUENTABANCOS.CLASECUENTA ='A' THEN SALDOs$mesfinal$s ELSE 0 END) , s$redondeo$s) CUENTAS_AHORROS,
         ROUND(SUM(CASE WHEN CUENTABANCOS.CLASECUENTA ='E' THEN SALDOs$mesfinal$s ELSE 0 END) , s$redondeo$s) ENCARGO_FIDUCIARIO,
         ROUND(SUM(CASE WHEN CUENTABANCOS.CLASECUENTA ='I' THEN SALDOs$mesfinal$s ELSE 0 END) , s$redondeo$s) INFIS,
         ROUND(SUM(CASE WHEN CUENTABANCOS.CLASECUENTA ='X' THEN SALDOs$mesfinal$s ELSE 0 END) , s$redondeo$s) OTROS_DEPOSITOS,
         ROUND(SUM(CASE WHEN CUENTABANCOS.CLASECUENTA ='Y' THEN SALDOs$mesfinal$s ELSE 0 END) , s$redondeo$s) OTROS_ENTIDAD,
         ROUND(SUM(CASE WHEN CUENTABANCOS.CLASECUENTA ='R' THEN SALDOs$mesfinal$s ELSE 0 END) , s$redondeo$s) RENDIMIENTOS,
         ROUND(SUM(CASE WHEN CUENTABANCOS.CLASECUENTA ='D' THEN SALDOs$mesfinal$s ELSE 0 END) , s$redondeo$s) CDT,
         ROUND(SUM(CASE WHEN CUENTABANCOS.CLASECUENTA ='T' THEN SALDOs$mesfinal$s ELSE 0 END) , s$redondeo$s) TES,
         ROUND(SUM(CASE WHEN CUENTABANCOS.CLASECUENTA ='N' THEN SALDOs$mesfinal$s ELSE 0 END) , s$redondeo$s) CARTERAS_COLECTIVAS,
         ROUND(SUM(CASE WHEN CUENTABANCOS.CLASECUENTA ='Z' THEN SALDOs$mesfinal$s ELSE 0 END) , s$redondeo$s) OTRAS_INVERSIONES
FROM FUENTE_RECURSOS INNER JOIN CUENTABANCOS 
  ON FUENTE_RECURSOS.COMPANIA = CUENTABANCOS.COMPANIA 
 AND FUENTE_RECURSOS.CODIGO   = CUENTABANCOS.RECURSOS 
 AND FUENTE_RECURSOS.ANO      = CUENTABANCOS.ANO
INNER JOIN PLAN_CONTABLE 
  ON CUENTABANCOS.COMPANIA   = PLAN_CONTABLE.COMPANIA 
 AND CUENTABANCOS.ANO        = PLAN_CONTABLE.ANO 
 AND CUENTABANCOS.IDCONTABLE = PLAN_CONTABLE.CODIGO 
WHERE PLAN_CONTABLE.COMPANIA = s$compania$s 
  AND PLAN_CONTABLE.ANO      = s$aniotrabajo$s
  AND CUENTABANCOS.ESTADO IN ('A')
  AND FUENTE_RECURSOS.CODIGO_FUT IS NOT NULL
GROUP BY FUENTE_RECURSOS.CODIGO_FUT 
ORDER BY FUENTE_RECURSOS.CODIGO_FUT
