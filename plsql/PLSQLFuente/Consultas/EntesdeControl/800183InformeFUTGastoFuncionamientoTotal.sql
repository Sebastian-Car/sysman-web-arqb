SELECT 
    UNIDADEJECUTORA_FUT ,
    FUENTE,
    SUM(PRESUPUESTO_INICIAL) PRESUPUESTO_INICIAL,
    SUM(PRESUPUESTO_DEFINITIVO) PRESUPUESTO_DEFINITIVO,
    SUM(COMPROMISOS) COMPROMISOS,
    SUM(OBLIGACIONES) OBLIGACIONES,
    SUM(PAGOS) PAGOS
  FROM (s$consultabase$s)
  GROUP BY  UNIDADEJECUTORA_FUT ,
    FUENTE
