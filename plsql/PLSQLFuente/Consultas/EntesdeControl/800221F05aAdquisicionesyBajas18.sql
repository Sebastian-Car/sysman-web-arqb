SELECT TO_CHAR(D_MOVIMIENTO.FECHA,'DD/MM/YYYY')                                        "Fecha Adquisición O Baja",
      (CASE WHEN MOVIMIENTO.CLASE_BODEGA_DESTINO IN('20')
             THEN 'ADQUISICION'
             ELSE 'BAJA'  
             END)                                                                      "Concepto",
       D_MOVIMIENTO.VALORTOTAL                                                         "Valor",
       INVENTARIO.NOMBRELARGO||'PLACA:'||D_MOVIMIENTO.SERIE                            "Detalle",
       (CASE WHEN MOVIMIENTO.CLASE_BODEGA_DESTINO IN('20')
             THEN GRUPO.CUENTAACTIVO
             ELSE GRUPO.CUENTAACTIVOI  
             END)                                                                      "Codigo Contable"
      
FROM D_MOVIMIENTO INNER JOIN MOVIMIENTO
       ON D_MOVIMIENTO.COMPANIA=MOVIMIENTO.COMPANIA
       AND D_MOVIMIENTO.TIPOMOVIMIENTO=MOVIMIENTO.TIPOMOVIMIENTO
       AND D_MOVIMIENTO.MOVIMIENTO=MOVIMIENTO.NUMERO
  INNER JOIN TIPOMOVIMIENTO 
      ON D_MOVIMIENTO.COMPANIA=TIPOMOVIMIENTO.COMPANIA
      AND D_MOVIMIENTO.TIPOMOVIMIENTO=TIPOMOVIMIENTO.CODIGO
INNER JOIN INVENTARIO 
      ON D_MOVIMIENTO.COMPANIA=INVENTARIO.COMPANIA
       AND D_MOVIMIENTO.ELEMENTO=INVENTARIO.CODIGOELEMENTO
LEFT  JOIN INVENTARIO GRUPO
      ON D_MOVIMIENTO.COMPANIA=GRUPO.COMPANIA
       AND SUBSTR(D_MOVIMIENTO.ELEMENTO,1,PCK_SYSMAN_UTL.FC_PAR(D_MOVIMIENTO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',-1,D_MOVIMIENTO.FECHA))=GRUPO.CODIGOELEMENTO
WHERE D_MOVIMIENTO.COMPANIA=s$compania$s
    AND MOVIMIENTO.ANO=s$ano$s 
    AND TO_NUMBER(TO_CHAR (D_MOVIMIENTO.FECHA,'MM'))  BETWEEN s$mesInicial$s AND s$mesFinal$s
    AND TIPOMOVIMIENTO.CLASE IN('E','D','S')
    AND TIPOMOVIMIENTO.CONCEPTO IN('C','L','DT','RM','DS')
    AND TIPOMOVIMIENTO.TIPOELEMENTO NOT IN('C')
ORDER BY D_MOVIMIENTO.FECHA

