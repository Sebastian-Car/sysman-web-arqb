WITH TEMP_036 AS
  (SELECT EXTRACT (MONTH FROM DETALLE_COMPROBANTE_PPTAL.FECHA) AS MES,
    V_PLAN_PRESUPUESTAL.APROPIACIONINICIAL                     AS APRINICIAL,
    EXTRACT (DAY FROM DETALLE_COMPROBANTE_PPTAL.FECHA)         AS DIA,
    EXTRACT (YEAR FROM DETALLE_COMPROBANTE_PPTAL.FECHA)        AS ANO,
    DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
    DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
    TIPO_COMPROBPP.CLASE,
    DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO,
    DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO,
    (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) AS VALORTOTAL,
    CASE
      WHEN TIPO_COMPROBPP.CLASE ='RES'
      OR TIPO_COMPROBPP.CLASE   ='DMR'
      OR TIPO_COMPROBPP.CLASE   ='ADR'
      THEN '7'
      WHEN TIPO_COMPROBPP.CLASE='REO'
      OR TIPO_COMPROBPP.CLASE  ='MRO'
      OR TIPO_COMPROBPP.CLASE  ='ARO'
      OR TIPO_COMPROBPP.CLASE  ='DRO'
      THEN '8'
      WHEN TIPO_COMPROBPP.CLASE='EGR'
      OR TIPO_COMPROBPP.CLASE  ='AEG'
      OR TIPO_COMPROBPP.CLASE  ='DEG'
      THEN '10'
      ELSE NULL
    END AS CLASECOMP,
    CASE
      WHEN TIPO_COMPROBPP.CLASE ='RES'
      OR TIPO_COMPROBPP.CLASE   ='DMR'
      OR TIPO_COMPROBPP.CLASE   ='ADR'
      THEN 'RC'
      WHEN TIPO_COMPROBPP.CLASE='REO'
      OR TIPO_COMPROBPP.CLASE  ='MRO'
      OR TIPO_COMPROBPP.CLASE  ='ARO'
      OR TIPO_COMPROBPP.CLASE  ='DRO'
      THEN 'OC'
      WHEN TIPO_COMPROBPP.CLASE='EGR'
      OR TIPO_COMPROBPP.CLASE  ='AEG'
      OR TIPO_COMPROBPP.CLASE  ='DEG'
      THEN 'TP'
      ELSE NULL
    END AS CLASELETRAS,
    DETALLE_COMPROBANTE_PPTAL.DESCRIPCION,
    V_PLAN_PRESUPUESTAL.NOMBRE,
    V_PLAN_PRESUPUESTAL.NIVEL1,
    V_PLAN_PRESUPUESTAL.NIVEL2,
    V_PLAN_PRESUPUESTAL.NIVEL3,
    V_PLAN_PRESUPUESTAL.NIVEL4,
    V_PLAN_PRESUPUESTAL.NIVEL5,
    V_PLAN_PRESUPUESTAL.NIVEL6,
    V_PLAN_PRESUPUESTAL.NIVEL7,
    V_PLAN_PRESUPUESTAL.ID,
    V_PLAN_PRESUPUESTAL.CODIGO,
    V_PLAN_PRESUPUESTAL.NATURALEZA,
    DETALLE_COMPROBANTE_PPTAL.FECHA,
    V_PLAN_PRESUPUESTAL.DESTINO,
    DETALLE_COMPROBANTE_PPTAL.NRO_DOCUMENTO,
    V_PLAN_PRESUPUESTAL.TIPOVIGENCIA,
    TERCERO.NOMBRE AS NOMBRETERCERO,
    V_PLAN_PRESUPUESTAL.FINALIDADGASTO
  FROM TERCERO
  INNER JOIN DETALLE_COMPROBANTE_PPTAL
  INNER JOIN TIPO_COMPROBPP
  ON DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
  AND DETALLE_COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA
  INNER JOIN V_PLAN_PRESUPUESTAL
  ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = V_PLAN_PRESUPUESTAL.COMPANIA
  AND DETALLE_COMPROBANTE_PPTAL.ANO     = V_PLAN_PRESUPUESTAL.ANO
  AND DETALLE_COMPROBANTE_PPTAL.CUENTA  = V_PLAN_PRESUPUESTAL.CODIGO
  INNER JOIN COMPROBANTE_PPTAL
  ON DETALLE_COMPROBANTE_PPTAL.COMPANIA     = COMPROBANTE_PPTAL.COMPANIA
  AND DETALLE_COMPROBANTE_PPTAL.ANO         = COMPROBANTE_PPTAL.ANO
  AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = COMPROBANTE_PPTAL.TIPO
  AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO
  ON TERCERO.NIT                            = DETALLE_COMPROBANTE_PPTAL.TERCERO
  AND TERCERO.SUCURSAL                      = DETALLE_COMPROBANTE_PPTAL.SUCURSAL
  AND TERCERO.COMPANIA                      = DETALLE_COMPROBANTE_PPTAL.COMPANIA
  WHERE V_PLAN_PRESUPUESTAL.NATURALEZA      ='D'
  AND DETALLE_COMPROBANTE_PPTAL.COMPANIA    = 's$compania$s'
  AND LENGTH(DETALLE_COMPROBANTE_PPTAL.CUENTA) BETWEEN 0 AND s$nivel$s
  AND DETALLE_COMPROBANTE_PPTAL.ANO = s$anio$s
  AND EXTRACT (MONTH FROM DETALLE_COMPROBANTE_PPTAL.FECHA) BETWEEN s$mes$s AND s$mes1$s
  AND V_PLAN_PRESUPUESTAL.ID BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
  ORDER BY ID,FECHA,CLASELETRAS,COMPROBANTE
  )
SELECT MES,
  APRINICIAL,
  DIA,
  ANO,
  COMPROBANTE,
  TIPO_CPTE,
  CLASE,
  VALORTOTAL,
  CASE
    WHEN CLASECOMP='7'
    THEN VALOR_DEBITO
    ELSE 0
  END AS INICIAL,
  CASE
    WHEN CLASECOMP='7'
    THEN VALOR_CREDITO
    ELSE 0
  END AS CANCELACION,
  CASE
    WHEN VALORTOTAL <0
    THEN '-'
    ELSE '+'
  END             AS SIGNO,
  ABS(VALORTOTAL) AS VALOR,
  CASE
    WHEN CLASECOMP ='7'
    THEN VALORTOTAL
    ELSE 0
  END AS RESERVACONSTITUIDA,
  CASE
    WHEN CLASELETRAS ='RC'
    THEN 1
    WHEN CLASELETRAS='OC'
    THEN 2
    ELSE 3
  END AS ORDENLETRAS,
  CASE
    WHEN CLASECOMP ='8'
    THEN VALOR_DEBITO
    ELSE 0
  END AS CONTRAIDAS,
  CASE
    WHEN CLASECOMP ='8'
    THEN VALOR_DEBITO
    ELSE 0
  END AS REVERSION,
  CASE
    WHEN CLASECOMP ='8'
    THEN VALORTOTAL
    ELSE 0
  END AS OBLIGACIONESCAUSADAS,
  CASE
    WHEN CLASECOMP ='10'
    THEN VALOR_DEBITO
    ELSE 0
  END AS EJECUCIONPAGOS,
  CASE
    WHEN CLASECOMP ='10'
    THEN VALOR_CREDITO
    ELSE 0
  END AS REVERSIONPAGOS,
  CASE
    WHEN CLASECOMP ='10'
    THEN VALORTOTAL
    ELSE 0
  END AS TOTALPAGOS,
  DESCRIPCION,
  NOMBRE,
  NIVEL1,
  NIVEL2,
  NIVEL3,
  NIVEL4,
  NIVEL5,
  NIVEL6,
  NIVEL7,
  ID,
  CODIGO,
  NATURALEZA,
  FECHA,
  DESTINO,
  NRO_DOCUMENTO,
  TIPOVIGENCIA,
  NOMBRETERCERO,
  FINALIDADGASTO
,
 CLASELETRAS
FROM TEMP_036
