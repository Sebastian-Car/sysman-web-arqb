SELECT DETALLE_COMPROBANTE_CNT.COMPANIA
, DETALLE_COMPROBANTE_CNT.ANO
, SUBSTR(CUENTA,1,6) AS GRUPO
, DETALLE_COMPROBANTE_CNT.TIPO_CPTE
, DETALLE_COMPROBANTE_CNT.COMPROBANTE
, DETALLE_COMPROBANTE_CNT.TERCERO
, DETALLE_COMPROBANTE_CNT.SUCURSAL
, TERCERO.NOMBRE
, DETALLE_COMPROBANTE_CNT.FECHA
, (CASE WHEN CLASEENTIDADOFICIAL=-1 THEN 'SI' ELSE 'NO' END) AS ENTIDAD_OFICIAL
, TERCERO.NIT_CEDULA AS CODIGO_EQUIVALENTE
, ROUND(SUM(CASE WHEN DETALLE_COMPROBANTE_CNT.NATURALEZA='D' THEN 
DETALLE_COMPROBANTE_CNT.VALOR_DEBITO-DETALLE_COMPROBANTE_CNT.VALOR_CREDITO ELSE
DETALLE_COMPROBANTE_CNT.VALOR_CREDITO-DETALLE_COMPROBANTE_CNT.VALOR_DEBITO END)/1000,0) AS VALOR
FROM TERCERO 
INNER JOIN DETALLE_COMPROBANTE_CNT 
        ON TERCERO.COMPANIA = DETALLE_COMPROBANTE_CNT.COMPANIA
        AND TERCERO.NIT = DETALLE_COMPROBANTE_CNT.TERCERO  
	    AND TERCERO.SUCURSAL = DETALLE_COMPROBANTE_CNT.SUCURSAL 
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA= 's$compania$s' 
  AND DETALLE_COMPROBANTE_CNT.ANO='s$aniotrabajo$s' 
  AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE<>'CIE' 
  AND SUBSTR(CUENTA,1,6) = 's$codigoCuenta$s'
  AND DETALLE_COMPROBANTE_CNT.FECHA <= s$corte$s
GROUP BY DETALLE_COMPROBANTE_CNT.COMPANIA
        , DETALLE_COMPROBANTE_CNT.ANO
        , SUBSTR(CUENTA,1,6)
        , DETALLE_COMPROBANTE_CNT.TIPO_CPTE
        , DETALLE_COMPROBANTE_CNT.COMPROBANTE
        , DETALLE_COMPROBANTE_CNT.TERCERO
        , DETALLE_COMPROBANTE_CNT.SUCURSAL
        , TERCERO.NOMBRE
        , DETALLE_COMPROBANTE_CNT.FECHA
        , (CASE WHEN CLASEENTIDADOFICIAL=-1 THEN 'SI' ELSE 'NO' END)
        , TERCERO.NIT_CEDULA
        , DETALLE_COMPROBANTE_CNT.NATURALEZA 
