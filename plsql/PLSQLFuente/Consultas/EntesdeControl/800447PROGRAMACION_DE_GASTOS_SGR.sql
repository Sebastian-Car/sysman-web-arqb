MERGE INTO CONSULTAS FIN USING (SELECT '800447PROGRAMACION_DE_GASTOS_SGR' INFORME ,TO_CLOB(q'[SELECT CODIGO,
--s$nombre$s
SECTOR,
FUENTE_FINANCIACION,
TIPO_RECURSO,
PROGRAMA,
BPIN,
TERCERO,
POLITICA_PUBLICA,
SITUACION_FONDOS,
TRUNC(APROPIACION_INICIAL) APROPIACION_INICIAL,
TRUNC(APROPIACION_DEFINITIVA) APROPIACION_DEFINITIVA
FROM(
SELECT CCPET.CODIGO,
       CCPET.NOMBRE,
       PLAN_PRESUPUESTAL.SECTOR SECTOR,
       PLAN_PRESUPUESTAL.CODIGOFUENTE FUENTE_FINANCIACION,
       PLAN_PRESUPUESTAL.TIPO_RECURSO TIPO_RECURSO,
       NVL(PLAN_PRESUPUESTAL.PROGRAMA,0) PROGRAMA,
       NVL(PLAN_PRESUPUESTAL.CODIGOBPIN,0) BPIN,
       NVL(PLAN_PRESUPUESTAL.TERCERO_CHIP,1) TERCERO,
       NVL(PLAN_PRESUPUESTAL.POLITCA_PUBLICA_CUIPO,'0') POLITICA_PUBLICA,
       CASE WHEN PLAN_PRESUPUESTAL.CONSITUACIONFONDOS NOT IN (0) THEN 'S' ELSE 'C' END SITUACION_FONDOS,
       SUM(SAL.APROPIACION_DEBITO) APROPIACION_INICIAL,
       SUM(SAL.APROPIACION_DEBITO + SAL.ADICION + SAL.REDUCCION +(SAL.TRASLADO_DEBITO - SAL.TRASLADO_CREDITO)+(SAL.APLAZAM_DEBITO - SAL.APLAZAM_CREDITO))APROPIACION_DEFINITIVA
       FROM PLAN_PRESUPUESTAL

INNER JOIN SALDO_AUX_PPTAL SAL 
     ON PLAN_PRESUPUESTAL.COMPANIA=SAL.COMPANIA
     AND PLAN_PRESUPUESTAL.ANO = SAL.ANO
     AND PLAN_PRESUPUESTAL.CODIGO = SAL.CODIGO
INNER JOIN TIPOCLASIFICADOR CCPET 
    ON PLAN_PRESUPUESTAL.COMPANIA = CCPET.COMPANIA 
     AND PLAN_PRESUPUESTAL.ANO = CCPET.ANO
     AND '010'= ccpet.claseclasificador
     AND PLAN_PRESUPUESTAL.CODIGOCCPETREGA = CCPET.CODIGO  
    WHERE CCPET.COMPANIA = s$compania$s
       AND CCPET.ANO = s$anio$s
       AND SAL.MES  <= s$mesfinal$s
       AND PLAN_PRESUPUESTAL.NATURALEZA = 'D'
       AND PLAN_PRESUPUESTAL.TIPO_RECURSO IS NOT NULL
  GROUP BY CCPET.CODIGO,
       CCPET.NOMBRE,
       PLAN_PRESUPUESTAL.SECTOR,
       PLAN_PRESUPUESTAL.CODIGOFUENTE,
       PLAN_PRESUPUESTAL.TIPO_RECURSO,
       NVL(PLAN_PRESUPUESTAL.CODIGOBPIN,0),
       PLAN_PRESUPUESTAL.CODIGOBPIN,
       NVL(PLAN_PRESUPUESTAL.TERCERO_CHIP,1),
       NVL(PLAN_PRESUPUESTAL.POLITCA_PUBLICA_CUIPO,'0'),
       NVL(PLAN_PRESUPUESTAL.PROGRAMA,0),
       CASE WHEN PLAN_PRESUPUESTAL.CONSITUACIONFONDOS NOT IN (0) THEN 'S' ELSE 'C' END
   HAVING SUM(Sal.Apropiacion_DEBITO) <>0 
       OR SUM(Sal.Apropiacion_DEBITO + Sal.Adicion +Sal.Reduccion + (SAL.TRASLADO_DEBITO- SAL.TRASLADO_CREDITO) + (Sal.Aplazam_Debito -Sal.Aplazam_Credito)) <>0 )CUIPO
       ORDER BY CODIGO]') CONSULTA, 99 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);