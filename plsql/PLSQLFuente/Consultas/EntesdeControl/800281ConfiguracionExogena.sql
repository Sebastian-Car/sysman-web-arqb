SELECT V_PLAN_CONTABLE.CODIGO "Cuenta",
  V_PLAN_CONTABLE.NOMBRE "Nombre",
  CLASECUENTA.DESCRIPCION "Clase Cuenta",
  CASE
    WHEN CONFIGURACION_EXOGENA.FORMATO = s$formato$s
    THEN CONFIGURACION_EXOGENA.CONCEPTO
    ELSE NULL
  END "Concepto",
  TO_CHAR(V_PLAN_CONTABLE.MOVIMIENTO) "Movimiento",
  V_PLAN_CONTABLE.EXDISTRITAL "Distrital",
  V_PLAN_CONTABLE.RETEPRACTICADA "Fuenta Practicada",
  V_PLAN_CONTABLE.RETEASUMIDA "Fuente Asumida",
  V_PLAN_CONTABLE.IVACOMUN "Iva Común",
  V_PLAN_CONTABLE.IVASIMPLIFICADO "Iva Simplificado",
  V_PLAN_CONTABLE.RETEICA "Ica",
  V_PLAN_CONTABLE.MOSTRARF1001 "Fomato 1001"
FROM V_PLAN_CONTABLE
INNER JOIN CLASECUENTA
ON V_PLAN_CONTABLE.CLASECUENTA = CLASECUENTA.CODIGO
INNER JOIN CONFIGURACION_EXOGENA
ON V_PLAN_CONTABLE.COMPANIA    = CONFIGURACION_EXOGENA.COMPANIA
AND V_PLAN_CONTABLE.ANO        = CONFIGURACION_EXOGENA.ANO
AND V_PLAN_CONTABLE.CODIGO     = CONFIGURACION_EXOGENA.CUENTA
WHERE V_PLAN_CONTABLE.COMPANIA = s$compania$s
AND V_PLAN_CONTABLE.ANO        = s$ano$s
ORDER BY V_PLAN_CONTABLE.CODIGO
