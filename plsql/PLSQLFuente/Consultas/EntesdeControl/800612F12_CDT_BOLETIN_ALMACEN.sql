      ]') || TO_CLOB(q'[ SUM(NVL(V_BASE_CUENTAALMACEN.VALOR_ENTRADAS,0)-NVL(V_BASE_CUENTAALMACEN.VALOR_SALIDAS,0)+  
           NVL(V_BASE_CUENTAALMACEN.AJUSTESDEBITO,0) +NVL(V_BASE_CUENTAALMACEN.AJUSTESCREDITO,0))  AS SUMADESALDOFINAL     
