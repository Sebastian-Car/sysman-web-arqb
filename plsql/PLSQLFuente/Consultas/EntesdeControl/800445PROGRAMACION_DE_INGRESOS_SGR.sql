MERGE INTO CONSULTAS FIN USING (SELECT '800445PROGRAMACION_DE_INGRESOS_SGR' INFORME ,TO_CLOB(q'[SELECT CODIGO,
--s$nombre$s
TIPO_RECURSO,
TERCERO,
POLITICA_PUBLICA,
SITUACION_FONDOS,
TRUNC(PRESUPUESTO_INICIAL) PRESUPUESTO_INICIAL,
TRUNC(PRESUPUESTO_DEFINITIVO) PRESUPUESTO_DEFINITIVO
FROM (
SELECT PLAN_PRESUPUESTAL.CODIGOCCPETREGA CODIGO,
  CCPET.NOMBRE,
  PLAN_PRESUPUESTAL.TIPO_RECURSO TIPO_RECURSO,
  PLAN_PRESUPUESTAL.TERCERO_CHIP  TERCERO, 
  NVL(PLAN_PRESUPUESTAL.POLITCA_PUBLICA_CUIPO,0) POLITICA_PUBLICA,
  CASE WHEN PLAN_PRESUPUESTAL.CONSITUACIONFONDOS NOT IN (0) THEN 'S' ELSE 'C' END SITUACION_FONDOS,
  SUM(APROPIACION_CREDITO) PRESUPUESTO_INICIAL,
  SUM(APROPIACION_CREDITO+ADICION+REDUCCION +(TRASLADO_CREDITO-TRASLADO_DEBITO)) PRESUPUESTO_DEFINITIVO
FROM PLAN_PRESUPUESTAL
INNER JOIN TIPOCLASIFICADOR CCPET 
ON PLAN_PRESUPUESTAL.ANO = CCPET.ANO
AND '010' = CCPET.CLASECLASIFICADOR
AND PLAN_PRESUPUESTAL.CODIGOCCPETREGA = CCPET.CODIGO
AND PLAN_PRESUPUESTAL.COMPANIA = CCPET.COMPANIA
INNER JOIN  SALDO_AUX_PPTAL SALDO_PLAN_PPTAL
 ON PLAN_PRESUPUESTAL.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
AND PLAN_PRESUPUESTAL.ANO      = SALDO_PLAN_PPTAL.ANO
AND PLAN_PRESUPUESTAL.CODIGO   = SALDO_PLAN_PPTAL.CODIGO
WHERE CCPET.COMPANIA = s$compania$s
AND CCPET.ANO        = s$anio$s
AND SALDO_PLAN_PPTAL.MES  <= s$mesfinal$s
AND PLAN_PRESUPUESTAL.NATURALEZA IN ('C')
AND PLAN_PRESUPUESTAL.TIPO_RECURSO IS NOT NULL 
GROUP BY PLAN_PRESUPUESTAL.CODIGOCCPETREGA,
  CCPET.NOMBRE,
PLAN_PRESUPUESTAL.TERCERO_CHIP,
  PLAN_PRESUPUESTAL.TIPO_RECURSO,
  PLAN_PRESUPUESTAL.POLITCA_PUBLICA_CUIPO,
  CASE WHEN PLAN_PRESUPUESTAL.CONSITUACIONFONDOS NOT IN (0) THEN 'S' ELSE 'C' END
) CUIPO
WHERE (PRESUPUESTO_INICIAL + PRESUPUESTO_DEFINITIVO ) NOT IN (0)
GROUP BY CODIGO,
s$nombre$s
TIPO_RECURSO,TERCERO,POLITICA_PUBLICA,
SITUACION_FONDOS,
PRESUPUESTO_INICIAL,
PRESUPUESTO_DEFINITIVO
ORDER BY CODIGO]') CONSULTA, 99 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);