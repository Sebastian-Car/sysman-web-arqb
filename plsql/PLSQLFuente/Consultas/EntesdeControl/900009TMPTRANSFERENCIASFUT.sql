SELECT 
  CODIGOSFUT.CODIGOFUT AS CODIGO,
  NVL(TERCERO.NIT_CEDULA,TERCERO.NIT) AS NOMBRE,
  SUM(CASE PLAN_PRESUPUESTAL.NATURALEZA WHEN 'D' THEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO ELSE  DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO-DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO END) AS PRESUPUESTO_DEFINITIVO
 FROM TERCERO 
INNER JOIN PLAN_PRESUPUESTAL 
INNER JOIN DETALLE_COMPROBANTE_PPTAL 
INNER JOIN TIPO_COMPROBPP 
	ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA   = TIPO_COMPROBPP.COMPANIA 
	AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE  = TIPO_COMPROBPP.CODIGO
	ON  PLAN_PRESUPUESTAL.COMPANIA           = DETALLE_COMPROBANTE_PPTAL.COMPANIA	
	AND PLAN_PRESUPUESTAL.ANO 			     = DETALLE_COMPROBANTE_PPTAL.ANO	
	AND PLAN_PRESUPUESTAL.CODIGO 			 = DETALLE_COMPROBANTE_PPTAL.CUENTA	
	ON  TERCERO.COMPANIA                     = DETALLE_COMPROBANTE_PPTAL.COMPANIA	
	AND TERCERO.NIT 						 = DETALLE_COMPROBANTE_PPTAL.TERCERO
	AND TERCERO.SUCURSAL                     = DETALLE_COMPROBANTE_PPTAL.SUCURSAL
INNER JOIN SALDO_AUX_PPTAL 
    ON  PLAN_PRESUPUESTAL.COMPANIA           = SALDO_AUX_PPTAL.COMPANIA	
	AND PLAN_PRESUPUESTAL.ANO 		    	 = SALDO_AUX_PPTAL.ANO
	AND PLAN_PRESUPUESTAL.CODIGO       	     = SALDO_AUX_PPTAL.CODIGO 	
INNER JOIN CODIGOSFUT
	ON  SALDO_AUX_PPTAL.COMPANIA             = CODIGOSFUT.COMPANIA
    AND SALDO_AUX_PPTAL.ANO                  = CODIGOSFUT.ANO
	AND SALDO_AUX_PPTAL.CODIGOFUT_H          = CODIGOSFUT.CODIGOFUT 
WHERE   SALDO_AUX_PPTAL.FUT_TRANSFERENCIA 			  		 = -1 
	AND PLAN_PRESUPUESTAL.ANO 								 = s$anoTrabajo$s 
	AND EXTRACT(MONTH FROM DETALLE_COMPROBANTE_PPTAL.FECHA)  <=s$mesFinal$s
	AND PLAN_PRESUPUESTAL.COMPANIA 							 ='s$compania$s' 
	AND CODIGOSFUT.ANO 										 = PLAN_PRESUPUESTAL.ANO
	AND TIPO_COMPROBPP.CLASE 								 IN ('ING','DIN','EGR','DEG') 
	AND PLAN_PRESUPUESTAL.NATURALEZA 						 = 'C'
 group by CODIGOSFUT.CODIGOFUT, NVL(TERCERO.NIT_CEDULA,TERCERO.NIT)
