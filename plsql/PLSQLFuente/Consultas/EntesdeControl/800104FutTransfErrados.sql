SELECT PLAN_PRESUPUESTAL.COMPANIA,
  PLAN_PRESUPUESTAL.ANO,
  SALDO_AUX_PPTAL.CODIGOFUT_H,
  SUM(SALDO_AUX_PPTAL.FUT_TRANSFERENCIA)  																												 AS SU,
  COUNT(PLAN_PRESUPUESTAL.CONSECUTIVO_FUT)																												 AS CU,
  CASE SUM(SALDO_AUX_PPTAL.FUT_TRANSFERENCIA) WHEN 0 THEN -1 ELSE CASE (COUNT(PLAN_PRESUPUESTAL.CONSECUTIVO_FUT))+(SUM(SALDO_AUX_PPTAL.FUT_TRANSFERENCIA)) WHEN 0 THEN -1 ELSE 0 END  END AS EXPR1
FROM PLAN_PRESUPUESTAL
INNER JOIN SALDO_AUX_PPTAL 
    ON  PLAN_PRESUPUESTAL.COMPANIA 	 = SALDO_AUX_PPTAL.COMPANIA
    AND PLAN_PRESUPUESTAL.ANO        = SALDO_AUX_PPTAL.ANO  
    AND PLAN_PRESUPUESTAL.CODIGO	 = SALDO_AUX_PPTAL.CODIGO 
GROUP BY PLAN_PRESUPUESTAL.COMPANIA,
		 PLAN_PRESUPUESTAL.ANO,
         SALDO_AUX_PPTAL.CODIGOFUT_H
HAVING PLAN_PRESUPUESTAL.COMPANIA=s$compania$s
	   AND PLAN_PRESUPUESTAL.ANO=s$anoTrabajo$s 
	   AND (CASE SUM(SALDO_AUX_PPTAL.FUT_TRANSFERENCIA) WHEN 0 THEN -1 ELSE CASE (COUNT(PLAN_PRESUPUESTAL.CONSECUTIVO_FUT))+(SUM(SALDO_AUX_PPTAL.FUT_TRANSFERENCIA)) WHEN 0 THEN -1 ELSE 0 END END )=0
