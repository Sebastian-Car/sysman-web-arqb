       PCK_SYSMAN_UTL.FC_NOMBRE_MES(s$mesFinal$s)                                   MESREPORTADO,
       DC.TIPO_CPTE||'-'||DC.COMPROBANTE                                       NO_DE_COMPROBANTE,
      DC.TERCERO_COM_NOM                                                 BENEFICIARIO,
       DC.TERCERO_COM                                                          CEDULA_NIT,
       DC.DESCRIPCION|| DC.TEXTO                                                DETALLE_DE_PAGO,
       DC.VLR_DOCUMENTO                                                        VALOR_COMPROBANTE_DE_PAGO,
       NVL(DESCUENTO,0)                                                         DESCUENTOS,
       VALOR_CREDITO                                                           NETO_PAGADO,
       CB.CUENTANUMERO                                                         NO_CUENTA,
       B.BANCO||'-'||B.NOMBREBANCO                                             BANCO,
       NVL(DC.NRO_DOCUMENTO,'ND')                                               NO_CHEQUE
FROM V_DETALLE_AUXILIAR_CNT DC
  LEFT JOIN COMPROBANTE_PPTAL  CP
    ON DC.COMPANIA=CP.COMPANIA
    AND DC.ANO=CP.ANO
    AND DC.TIPO_CPTE=CP.TIPO
    AND DC.COMPROBANTE=CP.NUMERO
  INNER JOIN (SELECT UNICO.COMPANIA,
       UNICO.ANO,
       UNICO.TIPO_CPTE,
       UNICO.COMPROBANTE,
       UNICO.CUENTA,
       CUENTABANCOS.BANCO,
       CUENTABANCOS.CODBANCO_SIA,
       CUENTABANCOS.CUENTANUMERO
FROM (SELECT DET.COMPANIA,
             DET.ANO,
             DET.TIPO_CPTE,
             DET.COMPROBANTE,
             MIN(DET.CUENTA) CUENTA
      FROM DETALLE_COMPROBANTE_CNT DET INNER JOIN CUENTABANCOS CUENTABANCOS
        ON DET.COMPANIA       = CUENTABANCOS.COMPANIA
       AND DET.ANO           = CUENTABANCOS.ANO
       AND DET.CUENTA        = CUENTABANCOS.IDCONTABLE
      INNER JOIN TIPO_COMPROBANTE TC
         ON DET.COMPANIA=TC.COMPANIA
         AND DET.TIPO_CPTE=TC.CODIGO
      WHERE DET.COMPANIA=s$compania$s
        AND DET.ANO=s$ano$s
        AND DET.MES BETWEEN s$mesInicial$s AND s$mesFinal$s
        AND TC.CLASE_CONTABLE IN ('E','G','A')
      GROUP BY DET.COMPANIA,
             DET.ANO,
             DET.TIPO_CPTE,
             DET.COMPROBANTE
   ) UNICO INNER JOIN CUENTABANCOS CUENTABANCOS
  ON UNICO.COMPANIA = CUENTABANCOS.COMPANIA
 AND UNICO.ANO      = CUENTABANCOS.ANO
 AND UNICO.CUENTA   = CUENTABANCOS.IDCONTABLE
 WHERE ESTADO IN('A')) CB
               ON DC.COMPANIA=CB.COMPANIA
               AND DC.ANO=CB.ANO
               AND DC.CODIGO_CUENTA=CB.CUENTA
  AND DC.TIPO_CPTE=CB.TIPO_CPTE
  AND DC.COMPROBANTE=CB.COMPROBANTE
INNER JOIN BANCO B
ON B.COMPANIA=CB.COMPANIA
AND B.BANCO=CB.BANCO
   LEFT JOIN (SELECT DC.COMPANIA,
                     DC.ANO,
                     DC.TIPO_CPTE,
                     DC.COMPROBANTE,
                    SUM(VALOR_CREDITO) DESCUENTO
             FROM DETALLE_COMPROBANTE_CNT DC
                    INNE]') || TO_CLOB(q'[R JOIN PLAN_CONTABLE P
                     ON DC.COMPANIA=P.COMPANIA
                     AND DC.CUENTA=P.CODIGO
AND DC.ANO=P.ANO
              WHERE DC.COMPANIA=s$compania$s
                   AND DC.ANO=s$ano$s
  AND DC.MES BETWEEN s$mesInicial$s AND s$mesFinal$s
                   AND P.TIPODESCUENTO_SIA IS NOT NULL
             GROUP BY DC.COMPANIA,
                      DC.ANO,
                      DC.TIPO_CPTE,
                      DC.COMPROBANTE) DESCUENTOS
          ON DC.COMPANIA=DESCUENTOS.COMPANIA
          AND DC.ANO=DESCUENTOS.ANO
          AND DC.TIPO_CPTE=DESCUENTOS.TIPO_CPTE
          AND DC.COMPROBANTE=DESCUENTOS.COMPROBANTE
WHERE DC.COMPANIA=s$compania$s
      AND DC.ANO=s$ano$s
      AND DC.MES BETWEEN s$mesInicial$s AND s$mesFinal$s
      AND DC.CLASE_CONTABLE IN ('E','G','A')
      AND CP.COMPANIA  IS NULL
      AND DC.CLASECUENTA IN('B')
 ORDER BY DC.FECHA
 ]') AS CONSULTA_VAL, 99 AS APLICACION_VAL, TO_CLOB(q'[]') AS CONSULTA_OPCIONAL_VAL, (SELECT MIN(ROWID) FROM CONSULTAS WHERE INFORME = '800169InformeF07RelacionDePagosSinAfectacion') AS RID_DESTINO  FROM DUAL ) INI ON (FIN.ROWID = INI.RID_DESTINO)  WHEN MATCHED THEN   UPDATE SET FIN.CONSULTA = INI.CONSULTA_VAL, FIN.APLICACION = INI.APLICACION_VAL, FIN.CONSULTA_OPCIONAL = INI.CONSULTA_OPCIONAL_VAL, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN   INSERT (INFORME, CONSULTA, APLICACION, CONSULTA_OPCIONAL, DATE_CREATED)   VALUES (INI.INFORME_VAL, INI.CONSULTA_VAL, INI.APLICACION_VAL, INI.CONSULTA_OPCIONAL_VAL, SYSDATE);