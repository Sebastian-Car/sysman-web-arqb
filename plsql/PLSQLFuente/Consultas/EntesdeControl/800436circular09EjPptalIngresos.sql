SELECT  
    RESUMENPPTO_P.ANO VIGENCIA,
    's$nitcompania$s' NIT_ENTIDAD, 
    's$nomcompania$s' NOMBRE_ENTIDAD, 
    RESUMENPPTO_P.CODIGO_CUENTA,
    RESUMENPPTO_P.NOMBRE_CUENTA,
    RESUMENPPTO_A.APRDEFINITIVA APROPIACION_INICIAL,
    RESUMENPPTO_P.ADICION,     
    RESUMENPPTO_A.APRDEFINITIVA + RESUMENPPTO_P.ADICION + RESUMENPPTO_P.REDUCCION + RESUMENPPTO_P.TRASLADO PPTO_DEFINITIVO,
    RESUMENPPTO_A.EJECUCIONPPT - RESUMENPPTO_A.REINTEGRO RECAUDOSANT,  
    RESUMENPPTO_P.EJECUCIONPPT - RESUMENPPTO_P.REINTEGRO RECAUDOSPERIODO,
    RESUMENPPTO_P.TRASLADO,  
    0 RECONOCIMIENTOS,
    (RESUMENPPTO_A.EJECUCIONPPT - RESUMENPPTO_A.REINTEGRO) + (RESUMENPPTO_P.EJECUCIONPPT - RESUMENPPTO_P.REINTEGRO) RECAUDOSTOTALES,    
    CASE WHEN (RESUMENPPTO_A.APRDEFINITIVA + RESUMENPPTO_P.ADICION + RESUMENPPTO_P.REDUCCION + RESUMENPPTO_P.TRASLADO + RESUMENPPTO_P.APLAZAMIENTO) = 0 THEN 0 ELSE 
    ROUND((RESUMENPPTO_A.EJECUCIONPPT - RESUMENPPTO_A.REINTEGRO + RESUMENPPTO_P.EJECUCIONPPT - RESUMENPPTO_P.REINTEGRO)/(RESUMENPPTO_A.APRDEFINITIVA + RESUMENPPTO_P.ADICION + RESUMENPPTO_P.REDUCCION + RESUMENPPTO_P.TRASLADO + RESUMENPPTO_P.APLAZAMIENTO), 4) *100 END PORCENTAJE_EJECUCION
    
    FROM     ( 
        SELECT  V_SALDO_PLAN_PPTAL.COMPANIA,  
                V_SALDO_PLAN_PPTAL.ANO,            
                V_SALDO_PLAN_PPTAL.ID CODIGO_CUENTA,
                V_PLAN_PRESUPUESTAL.NOMBRE NOMBRE_CUENTA, 
                SUM(  CASE   WHEN V_PLAN_PRESUPUESTAL.NATURALEZA = 'D' THEN   TRASLADO_DEBITO - TRASLADO_CREDITO  ELSE TRASLADO_CREDITO - TRASLADO_DEBITO END  ) TRASLADO,
                SUM(   CASE   WHEN V_PLAN_PRESUPUESTAL.NATURALEZA = 'D' THEN  EJE_PPT_DEBITO - EJE_PPT_CREDITO  ELSE   EJE_PPT_CREDITO - EJE_PPT_DEBITO  END  ) EJECUCIONPPT, 
                SUM(   CASE  WHEN V_PLAN_PRESUPUESTAL.NATURALEZA = 'D' THEN  APLAZAM_DEBITO - APLAZAM_CREDITO ELSE  APLAZAM_CREDITO - APLAZAM_DEBITO  END) APLAZAMIENTO,            
                SUM(V_SALDO_PLAN_PPTAL.ADICION) ADICION,             
                SUM(V_SALDO_PLAN_PPTAL.REDUCCION) REDUCCION ,   
                SUM(V_SALDO_PLAN_PPTAL.REINTEGRO) REINTEGRO                             
        FROM             V_PLAN_PRESUPUESTAL LEFT JOIN V_SALDO_PLAN_PPTAL 
                        ON V_PLAN_PRESUPUESTAL.COMPANIA = V_SALDO_PLAN_PPTAL.COMPANIA                                        
                        AND V_PLAN_PRESUPUESTAL.ANO = V_SALDO_PLAN_PPTAL.ANO                                        
                        AND V_PLAN_PRESUPUESTAL.ID = V_SALDO_PLAN_PPTAL.ID         
        WHERE   V_SALDO_PLAN_PPTAL.COMPANIA = s$compania$s          
            AND V_SALDO_PLAN_PPTAL.ANO = s$anio$s             
            AND V_SALDO_PLAN_PPTAL.ID BETWEEN 's$codinicial$s' AND 's$codfinal$s'             
            AND V_SALDO_PLAN_PPTAL.MES = s$mes$s    
        GROUP BY             V_SALDO_PLAN_PPTAL.COMPANIA,             V_SALDO_PLAN_PPTAL.ANO,             V_SALDO_PLAN_PPTAL.ID,           V_PLAN_PRESUPUESTAL.NOMBRE,  V_PLAN_PRESUPUESTAL.NATURALEZA     ) RESUMENPPTO_P     
    LEFT JOIN (        
        SELECT      V_SALDO_PLAN_PPTAL.COMPANIA,             
                    V_SALDO_PLAN_PPTAL.ANO,             
                    V_SALDO_PLAN_PPTAL.ID             ,
                    SUM( CASE  WHEN V_PLAN_PRESUPUESTAL.NATURALEZA = 'D' THEN    EJE_PPT_DEBITO - EJE_PPT_CREDITO ELSE   EJE_PPT_CREDITO - EJE_PPT_DEBITO   END ) EJECUCIONPPT,
                    SUM(CASE   WHEN V_PLAN_PRESUPUESTAL.NATURALEZA = 'D' THEN  APROPIACION_DEBITO - APROPIACION_CREDITO  ELSE   APROPIACION_CREDITO - APROPIACION_DEBITO    END  ) + SUM(V_SALDO_PLAN_PPTAL.ADICION) + SUM(V_SALDO_PLAN_PPTAL.REDUCCION) + SUM(                 CASE                     WHEN V_PLAN_PRESUPUESTAL.NATURALEZA = 'D' THEN                         APLAZAM_DEBITO - APLAZAM_CREDITO                     ELSE                         APLAZAM_CREDITO - APLAZAM_DEBITO                 END             ) + SUM(                 CASE                     WHEN V_PLAN_PRESUPUESTAL.NATURALEZA = 'D' THEN                         TRASLADO_DEBITO - TRASLADO_CREDITO                     ELSE                         TRASLADO_CREDITO - TRASLADO_DEBITO                 END             ) APRDEFINITIVA,
                    SUM(V_SALDO_PLAN_PPTAL.REINTEGRO) REINTEGRO                                
        FROM             V_PLAN_PRESUPUESTAL LEFT   JOIN V_SALDO_PLAN_PPTAL 
                ON V_PLAN_PRESUPUESTAL.COMPANIA = V_SALDO_PLAN_PPTAL.COMPANIA                                        
                AND V_PLAN_PRESUPUESTAL.ANO = V_SALDO_PLAN_PPTAL.ANO                                        
                AND V_PLAN_PRESUPUESTAL.ID = V_SALDO_PLAN_PPTAL.ID         
        WHERE     V_SALDO_PLAN_PPTAL.COMPANIA = s$compania$s            
              AND V_SALDO_PLAN_PPTAL.ANO = s$anio$s            
              AND V_SALDO_PLAN_PPTAL.ID BETWEEN 's$codinicial$s' AND 's$codfinal$s'             
              AND V_SALDO_PLAN_PPTAL.MES < s$mes$s
        GROUP BY      V_SALDO_PLAN_PPTAL.COMPANIA,             
                      V_SALDO_PLAN_PPTAL.ANO,             
                      V_SALDO_PLAN_PPTAL.ID,             
                      V_PLAN_PRESUPUESTAL.NATURALEZA     ) RESUMENPPTO_A 
    ON RESUMENPPTO_P.COMPANIA = RESUMENPPTO_A.COMPANIA                        
    AND RESUMENPPTO_P.ANO = RESUMENPPTO_A.ANO                        
    AND RESUMENPPTO_P.CODIGO_CUENTA = RESUMENPPTO_A.ID
   ORDER BY RESUMENPPTO_P.CODIGO_CUENTA