MERGE INTO CONSULTAS FIN USING (SELECT '800448EJECUCION_DE_GASTOS_SGR' INFORME ,TO_CLOB(q'[SELECT 
CODIGO,
--s$nombre$s
CODIGO_CPC,
SECTOR,
FUENTE_FINANCIACION,
TIPO_RECURSO,
PROGRAMATICO,
BPIN,
TERCERO,
 POLITICA_PUBLICA,
SITUACION_FONDOS,
TRUNC(COMPROMISOS) COMPROMISOS,
TRUNC(OBLIGACIONES) OBLIGACIONES,
TRUNC(PAGOS) PAGOS
FROM(
SELECT 
CCPET.COMPANIA,
CCPET.ANO,
CCPET.CODIGO,
CCPET.NOMBRE,
NVL(DETALLE_COMPROBANTE_PPTAL.CODIGO_CPC,'0') CODIGO_CPC,
NVL(DETALLE_COMPROBANTE_PPTAL.SECTOR,0) "SECTOR",
NVL(DETALLE_COMPROBANTE_PPTAL.FUENTE_CUIPO, 0) FUENTE_FINANCIACION,
P.TIPO_RECURSO TIPO_RECURSO,
NVL(DETALLE_COMPROBANTE_PPTAL.COD_PROD_CUIPO,0) PROGRAMATICO,
NVL(DETALLE_COMPROBANTE_PPTAL.CODIGO_BPIN,0)  BPIN,
CASE WHEN P.TRANSFERENCIA NOT IN (0) 
    THEN TERCERO.NIT
    ELSE '0'
 END TERCERO,
 NVL(DETALLE_COMPROBANTE_PPTAL.POLITICA_PUBLICA,'0') POLITICA_PUBLICA,
CASE WHEN P.CONSITUACIONFONDOS  NOT IN (0) THEN 'S' ELSE 'C' END SITUACION_FONDOS,
 SUM(CASE  WHEN TIPO_COMPROBPP.CLASE IN ('RES','ADR','DMR')
            THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)
            ELSE 0   
            END) AS COMPROMISOS,

         SUM(CASE  WHEN TIPO_COMPROBPP.CLASE IN ('REO','ARO','DRO')
              THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)
              ELSE 0   
              END) AS OBLIGACIONES,

             SUM(CASE  WHEN TIPO_COMPROBPP.CLASE IN ('EGR','ADG','DEG')
            THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)
            ELSE 0   
            END) AS PAGOS
  FROM PLAN_PRESUPUESTAL P 
INNER JOIN DETALLE_COMPROBANTE_PPTAL
 ON P.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA
AND P.ANO      = DETALLE_COMPROBANTE_PPTAL.ANO
AND P.CODIGO   = DETALLE_COMPROBANTE_PPTAL.CUENTA
INNER JOIN TIPOCLASIFICADOR CCPET 
    ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = CCPET.COMPANIA 
    AND DETALLE_COMPROBANTE_PPTAL.ANO = CCPET.ANO
    AND '010' = ccpet.claseclasificador
    AND DETALLE_COMPROBANTE_PPTAL.CODIGOCCPETREGA = CCPET.CODIGO    
INNER JOIN TIPO_COMPROBPP
 ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA
AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
INNER JOIN TERCERO 
 ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = TERCERO.COMPANIA
AND DETALLE_COMPROBANTE_PPTAL.TERCERO      = TERCERO.NIT
AND DETALLE_COMPROBANTE_PPTAL.SUCURSAL   = TERCERO.SUCURSAL 
  WHERE CCPET.COMPANIA = s$compania$s
    AND CCPET.ANO =  s$anio$s
    AND DETALLE_COMPROBANTE_PPTAL.MES  <= s$mesfinal$s
    AND P.NATURALEZA ='D'
    AND P.TIPO_RECURSO NOT IN (0)
GROUP BY CCPET.COMPANIA,
CCPET.ANO,
CCPET.CODIGO,
CCPET.NOMBRE,
NVL(DETALLE_COMPROBANTE_PPTAL.CODIGO_CPC,'0'),
DETALLE_COMPROBANTE_PPTAL.SECTOR,
DETALLE_COMPROBANTE_PPTAL.FUENTE_CUIPO,
P.TIPO_RECURSO,
NVL(DETALLE_COMPROBANTE_PPTAL.COD_PROD_CUIPO,0),
DETALLE_COMPROBANTE_PPTAL.CODIGO_BPIN ,
CASE WHEN P.TRANSFERENCIA NOT IN (0) 
    THEN TERCERO.NIT
    ELSE '0'
 END,
 NVL(DETALLE_COMPROBANTE_PPTAL.POLITICA_PUBLICA,'0'),
CASE WHEN P.CONSITUACIONFONDOS  NOT IN (0) THEN]') || TO_CLOB(q'[ 'S' ELSE 'C' END
) CUIPO 
WHERE (COMPROMISOS+OBLIGACIONES+PAGOS) NOT IN (0)
ORDER BY CODIGO]') CONSULTA, 99 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);