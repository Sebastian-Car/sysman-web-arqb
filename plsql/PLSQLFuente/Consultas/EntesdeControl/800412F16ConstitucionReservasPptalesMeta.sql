MERGE INTO CONSULTAS FIN USING (SELECT '800412F16ConstitucionReservasPptalesMeta' INFORME ,TO_CLOB(q'[SELECT CUENTA "Codigo Presupuestal",
       COMPROBANTE "Numero De Registro",
       TO_CHAR(DETALLE_COMPROBANTE_PPTAL.FECHA,'YYYY/MM/DD')  "Fecha Registro Presupuestal",
       DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO "Valor Registro Presupuestal", 
       TERCERO.NOMBRE "Beneficiario",
       NVL(NRO_DOCUMENTO,'ND') "Contrato O Resolución",
       DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO "Valor Reserva",     
       FUENTE_RECURSOS.CODIGO ||' '|| FUENTE_RECURSOS.NOMBRE "Fuente Financiacion"       
FROM DETALLE_COMPROBANTE_PPTAL INNER JOIN PLAN_PRESUPUESTAL
        ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA=PLAN_PRESUPUESTAL.COMPANIA
        AND DETALLE_COMPROBANTE_PPTAL.ANO=PLAN_PRESUPUESTAL.ANO
        AND DETALLE_COMPROBANTE_PPTAL.CUENTA=PLAN_PRESUPUESTAL.CODIGO
   INNER JOIN  TIPO_COMPROBPP 
         ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA=TIPO_COMPROBPP.COMPANIA
        AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE=TIPO_COMPROBPP.CODIGO
  INNER JOIN  TERCERO
         ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA=TERCERO.COMPANIA
        AND DETALLE_COMPROBANTE_PPTAL.TERCERO=TERCERO.NIT
        AND DETALLE_COMPROBANTE_PPTAL.SUCURSAL=TERCERO.SUCURSAL
  INNER JOIN  FUENTE_RECURSOS
      ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA=FUENTE_RECURSOS.COMPANIA
        AND DETALLE_COMPROBANTE_PPTAL.ANO=FUENTE_RECURSOS.ANO
        AND DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO=FUENTE_RECURSOS.CODIGO
    LEFT JOIN TIPO_FUENTE_SIA    
        ON  FUENTE_RECURSOS.CODIGO_SIA=TIPO_FUENTE_SIA.CODIGO
        
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA=s$compania$s
AND   DETALLE_COMPROBANTE_PPTAL.ANO=s$ano$s 
AND DETALLE_COMPROBANTE_PPTAL.MES <=s$mesFinal$s
AND PLAN_PRESUPUESTAL.TIPOVIGENCIA='RA'
AND TIPO_COMPROBPP.CLASE ='RES']') CONSULTA, 99 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);