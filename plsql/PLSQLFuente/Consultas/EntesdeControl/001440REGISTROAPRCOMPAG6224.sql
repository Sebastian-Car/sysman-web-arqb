  TEMP_036.CLASE,
  TEMP_036.CDPCONSTITUCION,
  TEMP_036.CDPREVERSION,
  TEMP_036.COMCONSTITUCION,
  TEMP_036.COMREVERSION,
  TEMP_036.OBLICONSTITUCION,
  TEMP_036.OBLIREVERSION,
  TEMP_036.PAGCONSTITUCION,
  TEMP_036.PAGREVERSION,
  TEMP_036.AUMENTOGASTOS,
  TEMP_036.DISMINUCIONGASTOS,
  TEMP_036.AUMENTOGASTOS-TEMP_036.DISMINUCIONGASTOS AS TOTALGASTOS,
  TEMP_036.FINALIDADGASTO, 
CASE WHEN TEMP_036.CLASELETRAS = 'CDP' THEN 
TEMP_036.DISPONIBILIDADACUM + TEMP_036.VALORTOTAL END  DISPONIBILIDADACUM,
CASE WHEN TEMP_036.CLASELETRAS = 'COM' THEN 
TEMP_036.COMPROMISOSACUM+TEMP_036.VALORTOTAL END  COMPROMISOSACUM,
CASE WHEN TEMP_036.CLASELETRAS = 'OB' THEN 
