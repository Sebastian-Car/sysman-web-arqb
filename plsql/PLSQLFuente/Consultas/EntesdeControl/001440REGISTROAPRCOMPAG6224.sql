  TEMP_036.OBLIGACIONES,
  TEMP_036.TOTALCOMPROMISOS-TEMP_036.OBLIGACIONES AS COMPROMISOSXCUMPLIR,
  TEMP_036.TOTALPAGOS,
  TEMP_036.OBLIGACIONES-TEMP_036.TOTALPAGOS AS OBLIGACIONESXPAGAR,
  TEMP_036.DESCRIPCION,
  TEMP_036.NIVEL1,
  TEMP_036.NIVEL2,
  TEMP_036.NIVEL3,
  TEMP_036.NIVEL4,
  TEMP_036.NIVEL5,
  TEMP_036.NIVEL6,
  TEMP_036.NIVEL7,
