WITH CLASEC AS (SELECT   TIPO_COMPROBPP.COMPANIA,
                         TIPO_COMPROBPP.CODIGO,
                         CASE WHEN TIPO_COMPROBPP.CLASE IN  ('DIS','DMD','ADD') THEN '11'
                              WHEN TIPO_COMPROBPP.CLASE IN  ('RES','DMR','ADR') THEN '13'
                              WHEN TIPO_COMPROBPP.CLASE IN  ('REO','MRO','ARO','DRO') THEN '15'
                              WHEN TIPO_COMPROBPP.CLASE IN  ('EGR','DEG','AEG') THEN '17'
                              WHEN TIPO_COMPROBPP.CLASE IN  ('TRA','ADC','RED','APL','Apr') THEN '10' END CLASECOMP,
                              
                         CASE TIPO_COMPROBPP.CLASE  WHEN 'DIS' THEN 1
                                                    WHEN 'ADD' THEN 2
                                                    WHEN 'DMD' THEN 3
                                                    WHEN 'RES' THEN 4
                                                    WHEN 'ADR' THEN 5
                                                    WHEN 'DMR' THEN 6
                                                    WHEN 'REO' THEN 7
                                                    WHEN 'ARO' THEN 8
                                                    WHEN 'DRO' THEN 9
                                                    WHEN 'EGR' THEN 10
                                                    WHEN 'AEG' THEN 11
                                                    WHEN 'DEG' THEN 12
                                                    ELSE 
                                                     13 
                                                    END                                                             AS ORDENCOMP
                          
                   FROM TIPO_COMPROBPP), TEMP AS(
SELECT 
DETALLE_COMPROBANTE_PPTAL.COMPANIA,
  DETALLE_COMPROBANTE_PPTAL.MES,
  DETALLE_COMPROBANTE_PPTAL.DIA,
  DETALLE_COMPROBANTE_PPTAL.ANO,
  DETALLE_COMPROBANTE_PPTAL.CUENTA,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
  TIPO_COMPROBPP.CLASE,
  (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) AS VALORTOTAL,
  CASE WHEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)<0 THEN '-' ELSE '+'END                                                     AS SIGNO,
  ABS((DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO))AS VALOR,
  CLASEC.ORDENCOMP,
   
  CASE CLASEC.CLASECOMP WHEN '10' THEN 'APNC'
                        WHEN '11' THEN 'CDP'
                        WHEN '13' THEN 'COM'
                        WHEN '15' THEN 'OB'
                        WHEN '17' THEN 'P' END     AS CLASELETRAS,
  
  CASE WHEN CLASEC.CLASECOMP='10' THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) ELSE 0 END AS NOCONDICIONADA,
  CASE WHEN CLASEC.CLASECOMP='11' THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) ELSE 0 END AS TOTALCDP,
  CASE WHEN CLASEC.CLASECOMP='13' THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) ELSE 0 END AS TOTALCOMPROMISOS,
  CASE WHEN CLASEC.CLASECOMP='15' THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) ELSE 0 END AS OBLIGACIONES,
  CASE WHEN CLASEC.CLASECOMP='17' THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) ELSE 0 END AS TOTALPAGOS,
  COMPROBANTE_PPTAL.DESCRIPCION,
  PLAN_PRESUPUESTAL.NOMBRE,
  PLAN_PRESUPUESTAL.NIVEL1,
  PLAN_PRESUPUESTAL.NIVEL2,
  PLAN_PRESUPUESTAL.NIVEL3,
  PLAN_PRESUPUESTAL.NIVEL4,
  PLAN_PRESUPUESTAL.NIVEL5,
  PLAN_PRESUPUESTAL.NIVEL6,
  PLAN_PRESUPUESTAL.NIVEL7,
  PLAN_PRESUPUESTAL.CODIGO,
  PLAN_PRESUPUESTAL.NATURALEZA,
  DETALLE_COMPROBANTE_PPTAL.FECHA,
  PLAN_PRESUPUESTAL.DESTINO,
  COMPROBANTE_PPTAL.NRO_DOCUMENTO                                                                                                                     AS NRO_DOCUMENTO,
  COMPROBANTE_PPTAL.TIPO_DOCUMENTO                                                                                                                    AS TIPODOCUMENTO,
  TERCERO.NOMBRE                                                                                                                                      AS NOMBRETERCERO,
  CASE WHEN  TIPO_COMPROBPP.CLASE ='DIS' THEN ABS((DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)) ELSE 0   END      AS CDPCONSTITUCION,   
  CASE WHEN  TIPO_COMPROBPP.CLASE ='DMD' THEN  ABS((DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)) ELSE 0  END      AS CDPREVERSION,   
  CASE WHEN  TIPO_COMPROBPP.CLASE ='RES' THEN  ABS((DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)) ELSE 0  END      AS COMCONSTITUCION,   
  CASE WHEN  TIPO_COMPROBPP.CLASE ='DMR' THEN  ABS((DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)) ELSE 0  END      AS COMREVERSION,   
  CASE WHEN  TIPO_COMPROBPP.CLASE ='REO' THEN  ABS((DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)) ELSE 0  END      AS OBLICONSTITUCION,   
  CASE WHEN  TIPO_COMPROBPP.CLASE ='DRO' THEN  ABS((DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)) ELSE 0  END      AS OBLIREVERSION,   
  CASE WHEN  TIPO_COMPROBPP.CLASE ='EGR' THEN  ABS((DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)) ELSE 0  END      AS PAGCONSTITUCION,   
  CASE WHEN  TIPO_COMPROBPP.CLASE ='DEG' THEN  ABS((DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)) ELSE 0  END      AS PAGREVERSION,   
  
  --PCK_GENERALES.FC_SALDOPPTAL(DETALLE_COMPROBANTE_PPTAL.COMPANIA,'AUMENTOGASTOS',DETALLE_COMPROBANTE_PPTAL.ANO,DETALLE_COMPROBANTE_PPTAL.ID,DETALLE_COMPROBANTE_PPTAL.MES)                                                                                    AS AUMENTOGASTOS,
  0    AS AUMENTOGASTOS,
  0                                                                                                                                      AS DISMINUCIONGASTOS,
  PLAN_PRESUPUESTAL.FINALIDADGASTO
FROM (((DETALLE_COMPROBANTE_PPTAL
INNER JOIN TIPO_COMPROBPP
ON (DETALLE_COMPROBANTE_PPTAL.COMPANIA   = TIPO_COMPROBPP.COMPANIA)
AND (DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO))
INNER JOIN  V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
ON (DETALLE_COMPROBANTE_PPTAL.CUENTA    = PLAN_PRESUPUESTAL.CODIGO)
AND (DETALLE_COMPROBANTE_PPTAL.ANO      = PLAN_PRESUPUESTAL.ANO)
AND (DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA))  
INNER JOIN COMPROBANTE_PPTAL
ON (DETALLE_COMPROBANTE_PPTAL.COMPANIA     = COMPROBANTE_PPTAL.COMPANIA)
AND (DETALLE_COMPROBANTE_PPTAL.ANO         = COMPROBANTE_PPTAL.ANO)
AND (DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = COMPROBANTE_PPTAL.TIPO)
AND (DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO)) 
INNER JOIN TERCERO
ON (DETALLE_COMPROBANTE_PPTAL.COMPANIA  = TERCERO.COMPANIA)
AND (DETALLE_COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL)
AND (DETALLE_COMPROBANTE_PPTAL.TERCERO  = TERCERO.NIT)
INNER JOIN CLASEC
ON CLASEC.COMPANIA  = TIPO_COMPROBPP.COMPANIA
AND CLASEC.CODIGO = TIPO_COMPROBPP.CODIGO
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA=s$compania$s
AND DETALLE_COMPROBANTE_PPTAL.ANO       =s$ano$s
AND PLAN_PRESUPUESTAL.NATURALEZA='D'), GEN AS(
SELECT 
((SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' THEN APROPIACION_DEBITO-APROPIACION_CREDITO ELSE APROPIACION_CREDITO-APROPIACION_DEBITO END)+(SUM(SALDO_PLAN_PPTAL.ADICION) +SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO+SALDO_PLAN_PPTAL.APLAZAM_DEBITO)))-((SUM(SALDO_PLAN_PPTAL.REDUCCION)* -1)+ SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO+SALDO_PLAN_PPTAL.APLAZAM_CREDITO)))           AS APROPIACIONVIGENTE,
SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD)                                                                                                                  AS DISPONIBILIDADACUM,
SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)+SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT)  AS COMPROMISOSACUM,
SUM(SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION)+SUM(SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION)                                                             AS OBLIGACIONESACUM,
SUM(CASE WHEN  PLAN_PRESUPUESTAL.NATURALEZA='D' THEN EJE_PPT_DEBITO-EJE_PPT_CREDITO ELSE EJE_PPT_CREDITO-EJE_PPT_DEBITO END )                         AS PAGOSACUM,
  SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,
  SALDO_PLAN_PPTAL.CODIGO,
  SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' THEN APROPIACION_DEBITO-APROPIACION_CREDITO ELSE APROPIACION_CREDITO-APROPIACION_DEBITO END)    AS APROPIADO,
  SUM(SALDO_PLAN_PPTAL.ADICION)                                                                                                                  AS ADICION,
  SUM(SALDO_PLAN_PPTAL.REDUCCION)                                                                                                                AS REDUCCION,
  SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO+SALDO_PLAN_PPTAL.APLAZAM_DEBITO)                                                                          AS CREDITO,
  SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO+SALDO_PLAN_PPTAL.APLAZAM_CREDITO)                                                                        AS CONTRACREDITO,
  (SUM(SALDO_PLAN_PPTAL.ADICION) +SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO+SALDO_PLAN_PPTAL.APLAZAM_DEBITO))                                         AS ADICIONES,
  ((SUM(SALDO_PLAN_PPTAL.REDUCCION)* -1)+ SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO+SALDO_PLAN_PPTAL.APLAZAM_CREDITO))                               AS REDUCCIONES, 
  SUM(CASE WHEN  PLAN_PRESUPUESTAL.NATURALEZA='D' THEN APLAZAM_DEBITO-APLAZAM_CREDITO ELSE APLAZAM_CREDITO-APLAZAM_DEBITO END)                          AS APLAZAMIENTOS,
  ((SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' THEN APROPIACION_DEBITO-APROPIACION_CREDITO ELSE APROPIACION_CREDITO-APROPIACION_DEBITO END)+(SUM(SALDO_PLAN_PPTAL.ADICION) +SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO+SALDO_PLAN_PPTAL.APLAZAM_DEBITO)))-((SUM(SALDO_PLAN_PPTAL.REDUCCION)* -1)+ SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO+SALDO_PLAN_PPTAL.APLAZAM_CREDITO))) AS SALDOAPROPIACION,
  SUM(CASE WHEN  PLAN_PRESUPUESTAL.NATURALEZA='D' THEN TRASLADO_DEBITO-TRASLADO_CREDITO ELSE TRASLADO_CREDITO-TRASLADO_DEBITO END )                     AS TRASLADOS,
  SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)                                                                                                                    AS SUMAREGCONT,
  SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)                                                                                                                 AS SUMAREGNOCONT,
  SUM(SALDO_PLAN_PPTAL.REG_REVERSION)                                                                                                                   AS SUMAREGREVERSION,
  SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)                                                                                                                  AS SUMAMODREGCONT,
  SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT)                                                                                                                AS SUMAMODREGNOCONT,
  SUM(SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION)                                                                                                             AS SUMAREGOBL,
  SUM(SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION)                                                                                                       AS SUMAMODIFREGOBL,
 SUM(SALDO_PLAN_PPTAL.INGRESOS_CAUSADOS)                                                                                                               AS INGRESOSCAUSADOSACUM,
  SUM(SALDO_PLAN_PPTAL.MODIF_INGRESOS_CAUSADOS)                                                                                                         AS MODIFINGRESOSCAUSADOSACUM,
  SUM(SALDO_PLAN_PPTAL.INGRESOS_EFECTIVO)                                                                                                               AS INGRESOSEFECTIVOACUM,
  SUM(SALDO_PLAN_PPTAL.INGRESOS_PAPELES)                                                                                                                AS INGRESOSPAPELESACUM,
  SUM(SALDO_PLAN_PPTAL.MODIF_INGRESOS_EFECTIVO)                                                                                                         AS MODIFINGRESOSEFECTIVOACUM,
  SUM(SALDO_PLAN_PPTAL.MODIF_INGRESOS_PAPELES)                                                                                                          AS MODIFINGRESOSPAPELESACUM,
  (SUM(SALDO_PLAN_PPTAL.INGRESOS_CAUSADOS) + SUM(SALDO_PLAN_PPTAL.MODIF_INGRESOS_CAUSADOS)+SUM(SALDO_PLAN_PPTAL.INGRESOS_EFECTIVO)+SUM(SALDO_PLAN_PPTAL.INGRESOS_PAPELES)+SUM(SALDO_PLAN_PPTAL.MODIF_INGRESOS_EFECTIVO)+SUM(SALDO_PLAN_PPTAL.MODIF_INGRESOS_PAPELES))        AS TOTALINGRESOS,
  SUM(SALDO_PLAN_PPTAL.PAC_APROPIADO)                                                                                                                  AS PAC,
  SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' THEN MODIF_PAC_DEBITO-MODIF_PAC_CREDITO ELSE MODIF_PAC_CREDITO-MODIF_PAC_DEBITO END)                        AS MODIFPAC,
  (SUM(SALDO_PLAN_PPTAL.PAC_APROPIADO)+SUM( CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' THEN MODIF_PAC_DEBITO-MODIF_PAC_CREDITO ELSE MODIF_PAC_CREDITO-MODIF_PAC_DEBITO END ))  AS PACACUM,
  PLAN_PRESUPUESTAL.NATURALEZA,
  PLAN_PRESUPUESTAL.TIPOVIGENCIA,
  SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' THEN APROPIACION_DEBITO-APROPIACION_CREDITO ELSE APROPIACION_CREDITO-APROPIACION_DEBITO END)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.REDUCCION)+SUM(CASE WHEN  PLAN_PRESUPUESTAL.NATURALEZA='D' THEN APLAZAM_DEBITO-APLAZAM_CREDITO ELSE APLAZAM_CREDITO-APLAZAM_DEBITO END)+SUM(CASE WHEN  PLAN_PRESUPUESTAL.NATURALEZA='D' THEN TRASLADO_DEBITO-TRASLADO_CREDITO ELSE TRASLADO_CREDITO-TRASLADO_DEBITO END ) AS APRDEFINITIVA,
  SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' THEN APROPIACION_DEBITO-APROPIACION_CREDITO ELSE APROPIACION_CREDITO-APROPIACION_DEBITO END)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO+SALDO_PLAN_PPTAL.APLAZAM_CREDITO)                     AS AUMENTO,
  ABS(SUM(SALDO_PLAN_PPTAL.REDUCCION))+SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO+SALDO_PLAN_PPTAL.APLAZAM_DEBITO)                                 AS DISMINUCION,
  SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' THEN APROPIACION_DEBITO-APROPIACION_CREDITO ELSE APROPIACION_CREDITO-APROPIACION_DEBITO END)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO+SALDO_PLAN_PPTAL.APLAZAM_DEBITO)                       AS AUMENTOGASTOS,
  ABS(SUM(SALDO_PLAN_PPTAL.REDUCCION))+SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO+SALDO_PLAN_PPTAL.APLAZAM_CREDITO)                           AS DISMINUCIONGASTOS                   
FROM 
PLAN_PRESUPUESTAL
INNER JOIN SALDO_PLAN_PPTAL
ON (PLAN_PRESUPUESTAL.CODIGO        = SALDO_PLAN_PPTAL.CODIGO)
AND (PLAN_PRESUPUESTAL.ANO      = SALDO_PLAN_PPTAL.ANO)
AND (PLAN_PRESUPUESTAL.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA)   
INNER JOIN TEMP 
ON TEMP.ANO         =  SALDO_PLAN_PPTAL.ANO
AND TEMP.MES-1     >=  SALDO_PLAN_PPTAL.MES
AND TEMP.CODIGO     =  SALDO_PLAN_PPTAL.CODIGO
GROUP BY SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,
  SALDO_PLAN_PPTAL.CODIGO,
  PLAN_PRESUPUESTAL.NATURALEZA,
  PLAN_PRESUPUESTAL.TIPOVIGENCIA), TEMP_036 AS (
SELECT  T.CODIGO ID,
  0 CONDICIONADA,
  T.COMPANIA,
  T.VALORTOTAL,
  T.NOMBRE,
  T.FECHA,
  T.MES,
  T.DIA,
  T.ANO,
  T.COMPROBANTE,
  CASE T.DESTINO WHEN 'F' THEN 'Funcionamiento'
                 WHEN 'I' THEN 'Inversion'
                 WHEN 'S' THEN 'Servicio de la Deuda' END DESTINO ,
  T.TIPODOCUMENTO,
  T.NRO_DOCUMENTO,
  T.SIGNO,
  T.VALOR,
  T.ORDENCOMP,
  T.CLASELETRAS,
  T.NOCONDICIONADA,
  T.TOTALCDP,
  T.NOCONDICIONADA-T.TOTALCDP AS APROPXAFECTAR,
  T.TOTALCOMPROMISOS,
  T.TOTALCDP-T.TOTALCOMPROMISOS AS CDPXCOMPROMETER,
  T.OBLIGACIONES,
  T.TOTALCOMPROMISOS-T.OBLIGACIONES AS COMPROMISOSXCUMPLIR,
  T.TOTALPAGOS,
  T.OBLIGACIONES-T.TOTALPAGOS AS OBLIGACIONESXPAGAR,
  T.DESCRIPCION,
  T.NIVEL1,
  T.NIVEL2,
  T.NIVEL3,
  T.NIVEL4,
  T.NIVEL5,
  T.NIVEL6,
  T.NIVEL7,
  T.NOMBRETERCERO,
  T.CLASE,
  T.CDPCONSTITUCION,
  T.CDPREVERSION,
  T.COMCONSTITUCION,
  T.COMREVERSION,
  T.OBLICONSTITUCION,
  T.OBLIREVERSION,
  T.PAGCONSTITUCION,
  T.PAGREVERSION,
  T.AUMENTOGASTOS,
  T.DISMINUCIONGASTOS,
  T.AUMENTOGASTOS-T.DISMINUCIONGASTOS AS TOTALGASTOS,
  T.FINALIDADGASTO , 
CASE WHEN T.CLASELETRAS = 'CDP' THEN 
GEN.DISPONIBILIDADACUM+(CASE WHEN T.SIGNO ='-' THEN T.VALOR*-1 ELSE T.VALOR END) END  DISPONIBILIDADACUM,
CASE WHEN T.CLASELETRAS = 'COM' THEN 
GEN.COMPROMISOSACUM+(CASE WHEN T.SIGNO ='-' THEN T.VALOR*-1 ELSE T.VALOR END)END  COMPROMISOSACUM,
CASE WHEN T.CLASELETRAS = 'OB' THEN 
GEN.OBLIGACIONESACUM+(CASE WHEN T.SIGNO ='-' THEN T.VALOR*-1 ELSE T.VALOR END) END  OBLIGACIONESACUM,
CASE WHEN T.CLASELETRAS = 'CDP' THEN 
GEN.PAGOSACUM+(CASE WHEN T.SIGNO ='-' THEN T.VALOR*-1 ELSE T.VALOR END) END PAGOSACUM,
CASE WHEN T.CLASELETRAS NOT IN ('CDP','COM','OB','CDP') THEN 
GEN.APROPIACIONVIGENTE+(CASE WHEN T.SIGNO ='-' THEN T.VALOR*-1 ELSE T.VALOR END) END APROPIACIONVIGENTE
FROM TEMP T INNER JOIN GEN ON
( T.COMPANIA=GEN.COMPANIA
  AND T.ANO =GEN.ANO 
  AND T.CODIGO=GEN.CODIGO 
  AND T.NATURALEZA=GEN.NATURALEZA)
WHERE T.COMPANIA=s$compania$s
AND T.ANO = s$ano$s
AND T.MES BETWEEN s$mesInicial$s AND s$mesFinal$s
AND T.CODIGO BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
AND T.CUENTA BETWEEN '0' AND 's$nivel$s')
SELECT 
  TEMP_036.COMPANIA,
  TEMP_036.ID ,
  SALDODECUENTAS.APROPIACIONVIGENTE NOCONDICIONADA,
  TEMP_036.VALORTOTAL,
  TEMP_036.SIGNO,
  TEMP_036.VALOR,
  0 CONDICIONADA,
  TEMP_036.NOMBRE,
  TEMP_036.FECHA,
  TEMP_036.MES,
  TEMP_036.DIA,
  TEMP_036.ANO,
  TEMP_036.COMPROBANTE,
  CASE TEMP_036.DESTINO WHEN 'F' THEN 'Funcionamiento'
                 WHEN 'I' THEN 'Inversion'
                 WHEN 'S' THEN 'Servicio de la Deuda' END DESTINO ,
  TEMP_036.TIPODOCUMENTO,
  TEMP_036.NRO_DOCUMENTO,
  
  TEMP_036.ORDENCOMP,
  TEMP_036.CLASELETRAS,
  
  SALDODECUENTAS.DISPONIBILIDAD TOTALCDP,
  TEMP_036.NOCONDICIONADA-TEMP_036.TOTALCDP AS APROPXAFECTAR,
  SALDODECUENTAS.TOTALCOMPROMISO TOTALCOMPROMISOS, 
