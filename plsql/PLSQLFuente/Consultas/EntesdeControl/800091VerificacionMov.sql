SELECT SALDO_AUX_PPTAL.COMPANIA
      , SALDO_AUX_PPTAL.ANO
      , SALDO_AUX_PPTAL.ID
      , SALDO_AUX_PPTAL.CODIGOSCHIP  CODIGOEQUIVALENTE
      , SUM(CASE WHEN SALDO_AUX_PPTAL.NATURALEZA='D' THEN SALDO_AUX_PPTAL.APROPIACION_DEBITO-SALDO_AUX_PPTAL.APROPIACION_CREDITO  ELSE SALDO_AUX_PPTAL.APROPIACION_CREDITO-SALDO_AUX_PPTAL.APROPIACION_DEBITO END) APROPIADO
      , SUM(CASE WHEN SALDO_AUX_PPTAL.NATURALEZA='D' THEN SALDO_AUX_PPTAL.MODIF_PAC_DEBITO-SALDO_AUX_PPTAL.MODIF_PAC_CREDITO ELSE SALDO_AUX_PPTAL.MODIF_PAC_CREDITO-SALDO_AUX_PPTAL.MODIF_PAC_DEBITO END) MODIFPAC
      , SUM(CASE WHEN SALDO_AUX_PPTAL.NATURALEZA='D' THEN SALDO_AUX_PPTAL.EJE_CNT_DEBITO-SALDO_AUX_PPTAL.EJE_CNT_CREDITO ELSE SALDO_AUX_PPTAL.EJE_CNT_CREDITO-SALDO_AUX_PPTAL.EJE_CNT_DEBITO END) EJECUCIONCNT
      , SUM(CASE WHEN SALDO_AUX_PPTAL.NATURALEZA='D' THEN SALDO_AUX_PPTAL.TRASLADO_DEBITO-SALDO_AUX_PPTAL.TRASLADO_CREDITO ELSE SALDO_AUX_PPTAL.TRASLADO_CREDITO-SALDO_AUX_PPTAL.TRASLADO_DEBITO END) TRASLADO
      , SUM(CASE WHEN SALDO_AUX_PPTAL.NATURALEZA='D' THEN SALDO_AUX_PPTAL.EJE_PPT_DEBITO-SALDO_AUX_PPTAL.EJE_PPT_CREDITO ELSE SALDO_AUX_PPTAL.EJE_PPT_CREDITO-SALDO_AUX_PPTAL.EJE_PPT_DEBITO END) EJECUCIONPPT
      , SUM(CASE WHEN SALDO_AUX_PPTAL.NATURALEZA='D' THEN SALDO_AUX_PPTAL.APLAZAM_DEBITO-SALDO_AUX_PPTAL.APLAZAM_CREDITO ELSE SALDO_AUX_PPTAL.APLAZAM_CREDITO-SALDO_AUX_PPTAL.APLAZAM_DEBITO END) APLAZAMIENTO
      , SUM(SALDO_AUX_PPTAL.ADICION) ADICION
      , SUM(SALDO_AUX_PPTAL.REDUCCION) REDUCCION
      , SUM(SALDO_AUX_PPTAL.DISPONIBILIDAD) DISPONIBILIDAD
      , SUM(SALDO_AUX_PPTAL.REG_CONTRACT) REG_CONTRAC
      , SUM(SALDO_AUX_PPTAL.REG_NO_CONTRACT) REG_NO_CONTRAC
      , SUM(SALDO_AUX_PPTAL.REG_REVERSION) REG_REVERSION
      , SUM(SALDO_AUX_PPTAL.VIGENCIAANTERIOR) VIGENCIAANTERIOR
      , SUM(SALDO_AUX_PPTAL.VIGENCIAFUTURA) VIGENCIAFUTURA
      , SUM(SALDO_AUX_PPTAL.PAC_APROPIADO) PAC_APROPIADO
      , SUM(SALDO_AUX_PPTAL.PAC_PROGRAMADO) PAC_PROGRAMADO
      , SUM(SALDO_AUX_PPTAL.MODIF_REG_CONT) MODIF_REG_CON
      , SUM(SALDO_AUX_PPTAL.MODIF_REG_NOCONT) MODIF_REG_NOCON
      , SUM(SALDO_AUX_PPTAL.REINTEGRO)  REINTEGRO
      , SALDO_AUX_PPTAL.NATURALEZA
      , SUM(SALDO_AUX_PPTAL.REG_NO_CONTRACT) + SUM(SALDO_AUX_PPTAL.MODIF_REG_NOCONT) REGNOCONTRACT   
      , SUM(SALDO_AUX_PPTAL.REG_CONTRACT) + SUM(SALDO_AUX_PPTAL.MODIF_REG_CONT) REGCONT               
      , SUM(SALDO_AUX_PPTAL.PAC_APROPIADO) + SUM(CASE WHEN SALDO_AUX_PPTAL.NATURALEZA='D' THEN SALDO_AUX_PPTAL.MODIF_PAC_DEBITO-SALDO_AUX_PPTAL.MODIF_PAC_CREDITO ELSE SALDO_AUX_PPTAL.MODIF_PAC_CREDITO-SALDO_AUX_PPTAL.MODIF_PAC_DEBITO END) PACTOTAL
      , SUM(SALDO_AUX_PPTAL.REGISTRO_OBLIGACION)  REO
      , SUM(SALDO_AUX_PPTAL.MODIF_REGISTRO_OBLIGACION) MODIFREO
      , SUM(SALDO_AUX_PPTAL.REGISTRO_OBLIGACION) + SUM(SALDO_AUX_PPTAL.MODIF_REGISTRO_OBLIGACION)  TOTALREO     
      , SUM(SALDO_AUX_PPTAL.MODIF_INGRESOS)  TOTALMODIFINGRESOS
      , SUM(CASE WHEN SALDO_AUX_PPTAL.NATURALEZA='D' THEN SALDO_AUX_PPTAL.EJE_PPT_DEBITO-SALDO_AUX_PPTAL.EJE_PPT_CREDITO ELSE SALDO_AUX_PPTAL.EJE_PPT_CREDITO-SALDO_AUX_PPTAL.EJE_PPT_DEBITO END) + SUM(SALDO_AUX_PPTAL.MODIF_INGRESOS) TOTALINGRESOS
      , SUM(SALDO_AUX_PPTAL.INGRESOS_CAUSADOS)  INGRESOSCAUSADOS
      , SUM(SALDO_AUX_PPTAL.MODIF_INGRESOS_CAUSADOS) MODIFICACIONICA
      , SUM(SALDO_AUX_PPTAL.INGRESOS_CAUSADOS) + SUM(SALDO_AUX_PPTAL.MODIF_INGRESOS_CAUSADOS)  TOTALICA
      , SUM(SALDO_AUX_PPTAL.PAC_EJECUTADO)  PACEJECUTADO_P
      , SUM(SALDO_AUX_PPTAL.EJE_PPT_DEBITO) CREDITOS
      , SUM(SALDO_AUX_PPTAL.EJE_PPT_CREDITO) CONTRACREDITOS
      , SALDO_AUX_PPTAL.ORIGENESPECIFICOINGRESOS
      , SALDO_AUX_PPTAL.DESTINACIONDELOSRECURSOS
      , SALDO_AUX_PPTAL.FINALIDADGASTO
      , SALDO_AUX_PPTAL.VIGENCIAGASTO
      , SALDO_AUX_PPTAL.DEPENDENCIASCHIP
      , SALDO_AUX_PPTAL.SITUACIONFONDOSSCHIP
      , SALDO_AUX_PPTAL.RECURSOSCHIP
      , (SUM(SALDO_AUX_PPTAL.REG_NO_CONTRACT) + SUM(SALDO_AUX_PPTAL.MODIF_REG_NOCONT))+(SUM(SALDO_AUX_PPTAL.REG_CONTRACT) + SUM(SALDO_AUX_PPTAL.MODIF_REG_CONT)) REGISTROSP
      , SUM(SALDO_AUX_PPTAL.TRASLADO_DEBITO) AS SUMADETRASLADO_DEBITO
      , SUM(SALDO_AUX_PPTAL.TRASLADO_CREDITO) AS SUMADETRASLADO_CREDITO
FROM SALDO_AUX_PPTAL 
WHERE SALDO_AUX_PPTAL.COMPANIA = s$compania$s
  AND SALDO_AUX_PPTAL.ANO = s$anio$s
  AND SALDO_AUX_PPTAL.CODIGOSCHIP IS NULL
GROUP BY SALDO_AUX_PPTAL.COMPANIA
        , SALDO_AUX_PPTAL.ANO
        , SALDO_AUX_PPTAL.ID
        , SALDO_AUX_PPTAL.CODIGOSCHIP
        , SALDO_AUX_PPTAL.NATURALEZA
        , SALDO_AUX_PPTAL.ORIGENESPECIFICOINGRESOS
        , SALDO_AUX_PPTAL.DESTINACIONDELOSRECURSOS
        , SALDO_AUX_PPTAL.FINALIDADGASTO
        , SALDO_AUX_PPTAL.VIGENCIAGASTO
        , SALDO_AUX_PPTAL.DEPENDENCIASCHIP
        , SALDO_AUX_PPTAL.SITUACIONFONDOSSCHIP
        , SALDO_AUX_PPTAL.RECURSOSCHIP
