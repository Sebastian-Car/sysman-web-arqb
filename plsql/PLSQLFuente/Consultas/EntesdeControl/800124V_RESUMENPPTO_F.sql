SELECT V_RESUMENPPTO_P.COMPANIA, 
       V_RESUMENPPTO_P.ANO, 
       V_RESUMENPPTO_P.ID, 
       V_RESUMENPPTO_P.CODIGO, 
       V_RESUMENPPTO_A.APRDEFINITIVA, 
       V_RESUMENPPTO_A.APROPIADO APROPIADOA,
       V_RESUMENPPTO_P.ADICION, 
       (V_RESUMENPPTO_P.ADICION + V_RESUMENPPTO_A.ADICION) TOTALADICION, 
       V_RESUMENPPTO_P.REDUCCION, 
       (V_RESUMENPPTO_P.REDUCCION + V_RESUMENPPTO_A.REDUCCION) TOTALREDUCCION, 
       V_RESUMENPPTO_P.TRASLADO, 
       (V_RESUMENPPTO_P.TRASLADO_DEBITO + V_RESUMENPPTO_A.TRASLADO_DEBITO)                      TRASLADO_DEBITO,
        (V_RESUMENPPTO_P.TRASLADO_CREDITO + V_RESUMENPPTO_A.TRASLADO_CREDITO)                   TRASLADO_CREDITO,
       V_RESUMENPPTO_P.APLAZAMIENTO, 
       (V_RESUMENPPTO_P.APLAZAM_CREDITO + V_RESUMENPPTO_A.APLAZAM_CREDITO)                      APLAZAM_CREDITO,
       (V_RESUMENPPTO_P.REINTEGRO + V_RESUMENPPTO_A.REINTEGRO)                                   REINTEGROSACUM,
       V_RESUMENPPTO_A.DISPONIBILIDAD                                                            DISPONIBILIDADA, 
       V_RESUMENPPTO_P.DISPONIBILIDAD                                                            DISPONIBILIDADP, 
       (V_RESUMENPPTO_A.DISPONIBILIDAD + V_RESUMENPPTO_P.DISPONIBILIDAD)                         DISPONIBILIDADF, 
       V_RESUMENPPTO_A.DISPONIBILIDAD_ADICION                                                    DISPONIBILIDAD_ADICIONA,  
       V_RESUMENPPTO_P.DISPONIBILIDAD_ADICION                                                    DISPONIBILIDAD_ADICIONP,  
       (V_RESUMENPPTO_A.DISPONIBILIDAD_ADICION + V_RESUMENPPTO_P.DISPONIBILIDAD_ADICION)         DISPONIBILIDAD_ADICIONF,  
       V_RESUMENPPTO_A.DISPONIBILIDAD_DISMINUCION                                                DISPONIBILIDAD_DISMINUCIONA,  
       V_RESUMENPPTO_P.DISPONIBILIDAD_DISMINUCION                                                DISPONIBILIDAD_DISMINUCIONP,  
       (V_RESUMENPPTO_A.DISPONIBILIDAD_DISMINUCION + V_RESUMENPPTO_P.DISPONIBILIDAD_DISMINUCION) DISPONIBILIDAD_DISMINUCIONF,  
       (V_RESUMENPPTO_A.COMPROMISOSACUM                                  )                       REGISTROSA, 
       (V_RESUMENPPTO_P.COMPROMISOSACUM                                  )                       REGISTROSP, 
       (V_RESUMENPPTO_A.COMPROMISOSACUM + V_RESUMENPPTO_P.COMPROMISOSACUM)                       REGISTROSF, 
       V_RESUMENPPTO_P.TOTALREO                                                                  REOP, 
       V_RESUMENPPTO_A.TOTALREO                                                                  REOA, 
       (V_RESUMENPPTO_P.TOTALREO + V_RESUMENPPTO_A.TOTALREO)                                     REOF, 
       (V_RESUMENPPTO_A.EJECUCIONPPT +V_RESUMENPPTO_P.EJECUCIONPPT)                              EJECUCIONPPTTOTAL, 
       V_RESUMENPPTO_A.EJECUCIONPPT - V_RESUMENPPTO_A.REINTEGRO                                  PAGOSA, 
       V_RESUMENPPTO_P.EJECUCIONPPT - V_RESUMENPPTO_P.REINTEGRO                                  PAGOSP, 
       ((V_RESUMENPPTO_A.EJECUCIONPPT-V_RESUMENPPTO_A.REINTEGRO)+
        (V_RESUMENPPTO_P.EJECUCIONPPT-V_RESUMENPPTO_P.REINTEGRO))                                PAGOSF, 
       V_RESUMENPPTO_P.PAC_APROPIADO                                                             PAC_APROPIADOP, 
       V_RESUMENPPTO_P.MODIFPAC                                                                  MODIFPACP, 
       V_RESUMENPPTO_P.PACTOTAL                                                                  PACTOTALP, 
       V_RESUMENPPTO_A.PACTOTAL                                                                  PACTOTALA, 
       (V_RESUMENPPTO_A.PACTOTAL       + V_RESUMENPPTO_P.PACTOTAL)                               PACTOTALF, 
       (V_RESUMENPPTO_P.PAC_PROGRAMADO + V_RESUMENPPTO_A.PAC_PROGRAMADO)                         PACPROGRAMADOF, 
       V_RESUMENPPTO_P.TOTALINGRESOS                                                             INGRESOSP, 
       V_RESUMENPPTO_A.TOTALINGRESOS                                                             INGRESOSA, 
       (V_RESUMENPPTO_P.TOTALINGRESOS + V_RESUMENPPTO_A.TOTALINGRESOS)                           INGRESOSF, 
       (V_RESUMENPPTO_P.EJECUCIONCNT  + V_RESUMENPPTO_A.EJECUCIONCNT)                            EJECUCIONCNTF, 
       V_RESUMENPPTO_P.INGRESOSCAUSADOS                                                          INGRESOSCAUSADOSP, 
       V_RESUMENPPTO_A.INGRESOSCAUSADOS                                                          INGRESOSCAUSADOSA, 
       (V_RESUMENPPTO_P.INGRESOSCAUSADOS + V_RESUMENPPTO_A.INGRESOSCAUSADOS)                     INGRESOSCAUSADOSF, 
       V_RESUMENPPTO_P.MODIFICACIONICA                                                           MODIFICACIONICAP, 
       V_RESUMENPPTO_A.MODIFICACIONICA                                                           MODIFICACIONICAA, 
       (V_RESUMENPPTO_P.MODIFICACIONICA + V_RESUMENPPTO_A.MODIFICACIONICA)                       MODIFICACIONICAF,        
       ((V_RESUMENPPTO_P.INGRESOSCAUSADOS + V_RESUMENPPTO_A.INGRESOSCAUSADOS) + 
        (V_RESUMENPPTO_P.MODIFICACIONICA + V_RESUMENPPTO_A.MODIFICACIONICA))                    TOTALICAF, 
       V_RESUMENPPTO_P.PACEJECUTADO PACEJECUTADO_P, 
       V_RESUMENPPTO_A.PACEJECUTADO PACEJECUTADOA
FROM V_RESUMENPPTO_P LEFT JOIN (s$vresumenpptoa$s) V_RESUMENPPTO_A 
 ON (V_RESUMENPPTO_P.COMPANIA = V_RESUMENPPTO_A.COMPANIA) 
AND (V_RESUMENPPTO_P.ANO      = V_RESUMENPPTO_A.ANO) 
AND (V_RESUMENPPTO_P.ID       = V_RESUMENPPTO_A.ID)
