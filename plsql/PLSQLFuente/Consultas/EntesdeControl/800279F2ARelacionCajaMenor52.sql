SELECT COMPROBANTE.CUENTA||'-'||COMPROBANTE.NOMBRE_CUENTA                             "Código_Contable",
       COMPROBANTE.TERCERO||'-'||COMPROBANTE.NOMBRE_TERCERO                           "Nombre Responsable",       
       TO_CHAR(DETALLE_COMPROBANTE_CNT.FECHA,'DD/MM/YYYY')                                                  "Fecha reembolso",
       DETALLE_COMPROBANTE_CNT.TIPO_CPTE||'-'|| DETALLE_COMPROBANTE_CNT.COMPROBANTE   "No Comprobante",
       SUM(VALOR_CREDITO)                                                              " Valor Bien o Servicio",
       DETALLE_COMPROBANTE_CNT.CUENTA||'-'||PLAN_CONTABLE.NOMBRE                     "Tipo"                     
FROM DETALLE_COMPROBANTE_CNT 
     INNER JOIN PLAN_CONTABLE
      ON DETALLE_COMPROBANTE_CNT.COMPANIA=PLAN_CONTABLE.COMPANIA
               AND DETALLE_COMPROBANTE_CNT.ANO=PLAN_CONTABLE.ANO
               AND DETALLE_COMPROBANTE_CNT.CUENTA=PLAN_CONTABLE.CODIGO 
       
       INNER JOIN ( SELECT DISTINCT COMPROBANTE_CNT.COMPANIA,
                       COMPROBANTE_CNT.ANO,COMPROBANTE_CNT.TIPO,
                       COMPROBANTE_CNT.NUMERO,
                       DETALLE_COMPROBANTE_CNT.CUENTA,
                       DETALLE_COMPROBANTE_CNT.TERCERO,
                       PLAN_CONTABLE.NOMBRE NOMBRE_CUENTA,
                       TERCERO.NOMBRE NOMBRE_TERCERO
              FROM DETALLE_COMPROBANTE_CNT INNER JOIN PLAN_CONTABLE
               ON DETALLE_COMPROBANTE_CNT.COMPANIA=PLAN_CONTABLE.COMPANIA
               AND DETALLE_COMPROBANTE_CNT.ANO=PLAN_CONTABLE.ANO
               AND DETALLE_COMPROBANTE_CNT.CUENTA=PLAN_CONTABLE.CODIGO
                   INNER JOIN COMPROBANTE_CNT
                        ON DETALLE_COMPROBANTE_CNT.COMPANIA=COMPROBANTE_CNT.COMPANIA
                       AND DETALLE_COMPROBANTE_CNT.ANO=COMPROBANTE_CNT.ANO
                       AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE=COMPROBANTE_CNT.TIPO
                       AND DETALLE_COMPROBANTE_CNT.COMPROBANTE=COMPROBANTE_CNT.NUMERO
                   INNER JOIN TERCERO
                        ON   DETALLE_COMPROBANTE_CNT.COMPANIA=TERCERO.COMPANIA
                        AND  DETALLE_COMPROBANTE_CNT.TERCERO=TERCERO.NIT
                        AND  DETALLE_COMPROBANTE_CNT.SUCURSAL=TERCERO.SUCURSAL
             WHERE DETALLE_COMPROBANTE_CNT.COMPANIA=s$compania$s
              AND DETALLE_COMPROBANTE_CNT.ANO=s$ano$s
			  AND PLAN_CONTABLE.CLASECUENTA IN('J')
              AND DETALLE_COMPROBANTE_CNT.MES BETWEEN s$mesInicial$s AND s$mesFinal$s 
              AND COMPROBANTE_CNT.TIPO_CAJA IN('02')
              ) COMPROBANTE
              
      ON  DETALLE_COMPROBANTE_CNT.COMPANIA=COMPROBANTE.COMPANIA  
      AND DETALLE_COMPROBANTE_CNT.ANO=COMPROBANTE.ANO
      AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE=COMPROBANTE.TIPO
      AND DETALLE_COMPROBANTE_CNT.COMPROBANTE=COMPROBANTE.NUMERO
 WHERE DETALLE_COMPROBANTE_CNT.COMPANIA=s$compania$s
 AND DETALLE_COMPROBANTE_CNT.ANO=s$ano$s
 AND PLAN_CONTABLE.CLASECUENTA NOT IN('J')
 AND DETALLE_COMPROBANTE_CNT.MES BETWEEN  s$mesInicial$s AND s$mesFinal$s 
 GROUP BY COMPROBANTE.CUENTA||'-'||COMPROBANTE.NOMBRE_CUENTA,
      COMPROBANTE.TERCERO||'-'||COMPROBANTE.NOMBRE_TERCERO,
     TO_CHAR(DETALLE_COMPROBANTE_CNT.FECHA,'DD/MM/YYYY') ,
      DETALLE_COMPROBANTE_CNT.TIPO_CPTE||'-'|| DETALLE_COMPROBANTE_CNT.COMPROBANTE,
      DETALLE_COMPROBANTE_CNT.CUENTA||'-'||PLAN_CONTABLE.NOMBRE
  HAVING SUM(VALOR_CREDITO) NOT IN(0)
ORDER BY COMPROBANTE.CUENTA||'-'||COMPROBANTE.NOMBRE_CUENTA
