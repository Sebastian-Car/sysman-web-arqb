WITH V_RESUMENPPTOI_SIRECI AS
    (SELECT  CODIGOSIRECI CODIGOEQUIVALENTE,
             SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' 
             	     THEN APROPIACION_DEBITO-APROPIACION_CREDITO 
             	     ELSE APROPIACION_CREDITO-APROPIACION_DEBITO 
             	END) APROPIADO,
             SUM(SALDO_AUX_PPTAL.ADICION) ADICION,
             SUM(ABS(SALDO_AUX_PPTAL.REDUCCION)) REDUCCION,
             SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' 
             	     THEN APROPIACION_DEBITO-APROPIACION_CREDITO 
             	     ELSE APROPIACION_CREDITO-APROPIACION_DEBITO 
             	END)
             +SUM(SALDO_AUX_PPTAL.ADICION)
             -SUM(ABS(SALDO_AUX_PPTAL.REDUCCION)) DEFINITIVO,
             SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' 
             	     THEN EJE_PPT_DEBITO-EJE_PPT_CREDITO 
             	     ELSE EJE_PPT_CREDITO-EJE_PPT_DEBITO 
             	END + MODIF_INGRESOS_EFECTIVO+MODIF_INGRESOS_PAPELES) RECAUDOSACUMULADOS,
             SUM(SALDO_AUX_PPTAL.VIGENCIAANTERIOR) VIGENCIAANTERIOR,
             SUM(SALDO_AUX_PPTAL.RECONOCIMIENTOS) RECON,
             SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' 
             	     THEN EJE_PPT_DEBITO-EJE_PPT_CREDITO 
             	     ELSE EJE_PPT_CREDITO-EJE_PPT_DEBITO 
             	END )+SUM(SALDO_AUX_PPTAL.MODIF_INGRESOS) TOTALINGRESOS 
      FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL 
          INNER JOIN SALDO_AUX_PPTAL 
              ON  PLAN_PRESUPUESTAL.COMPANIA = SALDO_AUX_PPTAL.COMPANIA
              AND PLAN_PRESUPUESTAL.ANO      = SALDO_AUX_PPTAL.ANO
              AND PLAN_PRESUPUESTAL.CODIGO   = SALDO_AUX_PPTAL.CODIGO 
     WHERE PLAN_PRESUPUESTAL.COMPANIA   =  s$compania$s
       AND PLAN_PRESUPUESTAL.ANO        =  s$ano$s 
       AND PLAN_PRESUPUESTAL.NATURALEZA =  'C'
       AND SALDO_AUX_PPTAL.MES          <= 12 
       AND CODIGOSIRECI                 IS NOT NULL
     GROUP BY  SALDO_AUX_PPTAL.COMPANIA
              ,SALDO_AUX_PPTAL.ANO
              ,CODIGOSIRECI)  
SELECT  CODIGOEQUIVALENTE CONCEPTO, 
        APROPIADO  "PRESUPUESTO INICIAL" ,
        ADICION, 
        REDUCCION,
        DEFINITIVO "PRESUPUESTO DEFINITIVO",  
        0 "RECAUDO/RENDIMIENTO FINANCIERO",
        0 "RECAUDO/SALDOS VIGENCIA ANTERI", 
        0 "RECAUDO/GIROS RECIBI NIVEL NAL", 
        0 "RECAUDO/GIROS RECIBI NIVEL DEP", 
        RECAUDOSACUMULADOS "RECAUDO/OTRO RECAUDO EFECTIVO", 
        0 "RECAUDO/TOTAL RECAUDO EFECTIVO"
 FROM V_RESUMENPPTOI_SIRECI
WHERE TRUNC(CASE WHEN TOTALINGRESOS>=0 
	             THEN TOTALINGRESOS+0.501 
	             ELSE TOTALINGRESOS-0.501 
	        END) NOT IN (0)  
  OR  TRUNC(CASE WHEN RECON>=0 
  	             THEN RECON+0.501 
  	             ELSE RECON-0.501 
  	        END) NOT IN (0) 
  OR  TRUNC(CASE WHEN VIGENCIAANTERIOR>=0 
  	             THEN VIGENCIAANTERIOR+0.501

  	             ELSE VIGENCIAANTERIOR-0.501 
  	        END) NOT IN (0) 
ORDER BY CODIGOEQUIVALENTE
