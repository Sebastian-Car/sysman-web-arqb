SELECT TIPO_PAGOSIA_NOM.DESCRIPCION                       TIPO_PAGOSIA,
       CONCEPTOS.NOMBRE_CONCEPTO                          CONCEPTO, 
       SUM(CASE WHEN ESCALAFON.CODSIDEF IN(6)
           THEN HISTORICOS.VALOR
           ELSE 0
           END)                                           ASISTENCIAL,
        SUM(CASE WHEN ESCALAFON.CODSIDEF IN(5)
           THEN HISTORICOS.VALOR
           ELSE 0
           END)                                            TÉCNICO,
         SUM(CASE WHEN ESCALAFON.CODSIDEF IN(4)
           THEN HISTORICOS.VALOR
           ELSE 0
           END)                                            PROFESIONAL,
         SUM(CASE WHEN ESCALAFON.CODSIDEF IN(2)
           THEN HISTORICOS.VALOR
           ELSE 0
           END)                                            ASESOR,
         SUM(CASE WHEN ESCALAFON.CODSIDEF IN(1)
           THEN HISTORICOS.VALOR
           ELSE 0
           END)                                            DIRECTIVO   
FROM PERSONAL INNER JOIN HISTORICOS
     ON PERSONAL.COMPANIA           =HISTORICOS.COMPANIA
     AND PERSONAL.ID_DE_EMPLEADO    =HISTORICOS.ID_DE_EMPLEADO
  INNER JOIN  PERSONAL_HISTORICO
         ON PERSONAL_HISTORICO.COMPANIA          =HISTORICOS.COMPANIA
         AND PERSONAL_HISTORICO.ID_DE_PROCESO    =HISTORICOS.ID_DE_PROCESO
         AND PERSONAL_HISTORICO.ANO              =HISTORICOS.ANO
         AND PERSONAL_HISTORICO.MES              =HISTORICOS.MES
         AND PERSONAL_HISTORICO.PERIODO          =HISTORICOS.PERIODO
          AND PERSONAL_HISTORICO.ID_DE_EMPLEADO  =HISTORICOS.ID_DE_EMPLEADO
 INNER JOIN PERIODOS
         ON  PERSONAL_HISTORICO.COMPANIA=PERIODOS.COMPANIA
         AND PERSONAL_HISTORICO.ID_DE_PROCESO=PERIODOS.ID_DE_PROCESO
         AND PERSONAL_HISTORICO.ANO=PERIODOS.ANO
         AND PERSONAL_HISTORICO.MES=PERIODOS.MES
         AND PERSONAL_HISTORICO.PERIODO=PERIODOS.PERIODO
  INNER JOIN CONCEPTOS
    ON HISTORICOS.COMPANIA      =CONCEPTOS.COMPANIA
     AND HISTORICOS.ID_DE_CONCEPTO  =CONCEPTOS.ID_DE_CONCEPTO
   INNER  JOIN ESCALAFON
     ON PERSONAL_HISTORICO.COMPANIA=ESCALAFON.COMPANIA
     AND PERSONAL_HISTORICO.ESCALAFON=ESCALAFON.CODIGO
    LEFT JOIN TIPO_PAGOSIA_NOM
     ON CONCEPTOS.TIPO_PAGOSIA=TIPO_PAGOSIA_NOM.CODIGO
WHERE PERSONAL_HISTORICO.COMPANIA= s$compania$s
	AND PERSONAL_HISTORICO.ANO=s$ano$s
                     AND PERSONAL_HISTORICO.MES BETWEEN  s$mesInicial$s AND    s$mesFinal$s
	AND  CONCEPTOS.CLASE IN (3)
                     AND PERIODOS.ACUMULADO  NOT IN (0)
	AND CONCEPTOS.TIPO_PAGOSIA IS NOT NULL
GROUP BY CONCEPTOS.ID_DE_CONCEPTO,CONCEPTOS.NOMBRE_CONCEPTO,TIPO_PAGOSIA_NOM.DESCRIPCION,CONCEPTOS.TIPO_PAGOSIA
ORDER BY CONCEPTOS.TIPO_PAGOSIA,CONCEPTOS.ID_DE_CONCEPTO
