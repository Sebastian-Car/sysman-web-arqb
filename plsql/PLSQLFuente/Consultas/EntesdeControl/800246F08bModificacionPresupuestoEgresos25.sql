MERGE INTO CONSULTAS FIN USING (SELECT '800246F08bModificacionPresupuestoEgresos25' INFORME ,TO_CLOB(q'[SELECT PLAN_PPTAL_CONFIG.CODIGO_SIA||DETALLE_COMPROBANTE_PPTAL.CUENTA       "Código Rubro Presupuestal",
       DETALLE_COMPROBANTE_PPTAL.NRO_DOCUMENTO                              "Acto Administrativo",
       TO_CHAR(DETALLE_COMPROBANTE_PPTAL.FECHA,'DD/MM/YYYY')                "Fecha",
      SUM(CASE WHEN TIPO_COMPROBPP.CLASE='ADC'
                     THEN VALOR_DEBITO
                     ELSE
                     0 END)                                                  "Adición",
      SUM(CASE WHEN TIPO_COMPROBPP.CLASE='RED'
                     THEN VALOR_CREDITO
                     ELSE
                     0 END)                                                  "Reducción",
      SUM(CASE WHEN TIPO_COMPROBPP.CLASE='TRA'
                     THEN VALOR_DEBITO
                     ELSE
                     0 END)                                                  "Crédito",               
       SUM(CASE WHEN TIPO_COMPROBPP.CLASE='TRA'
                     THEN VALOR_CREDITO
                     ELSE
                    0 END)                                                  "Contracrédito",
       SUM(CASE WHEN TIPO_COMPROBPP.CLASE='APL'
                     THEN VALOR_CREDITO
                     ELSE
                     0 END)                                                 "Aplazamiento",
	 SUM(CASE WHEN TIPO_COMPROBPP.CLASE='APL'
                     THEN VALOR_DEBITO
                     ELSE
                     0 END)                                                 "Desaplazamiento"  			 
FROM DETALLE_COMPROBANTE_PPTAL INNER JOIN  PLAN_PPTAL_CONFIG
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA=  PLAN_PPTAL_CONFIG.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO=  PLAN_PPTAL_CONFIG.ANO
    AND DETALLE_COMPROBANTE_PPTAL.CUENTA=  PLAN_PPTAL_CONFIG.CODIGO
    AND DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO=  PLAN_PPTAL_CONFIG.CENTRO_COSTO
    AND DETALLE_COMPROBANTE_PPTAL.AUXILIAR=  PLAN_PPTAL_CONFIG.AUXILIAR
    AND DETALLE_COMPROBANTE_PPTAL.REFERENCIA=  PLAN_PPTAL_CONFIG.REFERENCIA 
    AND DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO=  PLAN_PPTAL_CONFIG.FUENTE_RECURSO
 INNER JOIN  PLAN_PRESUPUESTAL
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA=  PLAN_PRESUPUESTAL.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO=  PLAN_PRESUPUESTAL.ANO   
     AND DETALLE_COMPROBANTE_PPTAL.CUENTA=  PLAN_PRESUPUESTAL.CODIGO
 INNER JOIN TIPO_COMPROBPP
      ON DETALLE_COMPROBANTE_PPTAL.COMPANIA=  TIPO_COMPROBPP.COMPANIA
      AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE=  TIPO_COMPROBPP.CODIGO
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA     = s$compania$s
  AND DETALLE_COMPROBANTE_PPTAL.ANO          = s$ano$s
  AND TIPO_COMPROBPP.CLASE                   IN ('ADC','RED','TRA','APL')
  AND PLAN_PPTAL_CONFIG.NATURALEZA IN('D')
  AND PLAN_PRESUPUESTAL.REGALIAS IN (0)
  AND DETALLE_COMPROBANTE_PPTAL.MES          BETWEEN  s$mesInicial$s AND  s$mesFinal$s
GROUP BY PLAN_PPTAL_CONFIG.CODIGO_SIA||DETALLE_COMPROBANTE_PPTAL.CUENTA,
       DETALLE_COMPROBANTE_PPTAL.NRO_DOCUMENTO,
       DETALLE]') || TO_CLOB(q'[_COMPROBANTE_PPTAL.FECHA
ORDER BY PLAN_PPTAL_CONFIG.CODIGO_SIA||DETALLE_COMPROBANTE_PPTAL.CUENTA]') CONSULTA, 99 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
