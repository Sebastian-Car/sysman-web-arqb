MERGE INTO CONSULTAS FIN USING (SELECT '800569V_CGR_Relacion_cdpAppUI' INFORME ,TO_CLOB(q'[SELECT detalleCdp,
        Vigencia,
        macroCampo,
        codigoPresupuestal,
        fuenteFin,
        nombreRubro,
        fechaCdp,
        valorCdp,
        numero_cdp numeroCdp

FROM(
SELECT TO_CHAR(dcp.COMPROBANTE ) numero_cdp,
  	 REPLACE(REPLACE(NVL(dcp.DESCRIPCION,'NA'), CHR(13), ' ') , CHR(10), ' ') detalleCdp,
     TIPOVIGENCIA.EQUIVALENTE_APPUI Vigencia,
               '2' macroCampo,
     CODIGO_CCPET.CODIGO codigoPresupuestal,
	 fr.CODIGO_CLEOPATRA   fuenteFin, 
   	CODIGO_CCPET.NOMBRE nombreRubro,
  	NVL(TO_CHAR(dcp.FECHA,'YYYY-MM-DD'),'1900-01-01')  fechaCdp,
	(NVL(dcp.VALOR_DEBITO,0) - NVL(dcp.VALOR_CREDITO,0)) valorCdp

FROM
	COMPROBANTE_PPTAL cp
INNER JOIN DETALLE_COMPROBANTE_PPTAL dcp 
ON 	cp.COMPANIA = dcp.COMPANIA
	AND cp.ANO = dcp.ANO
	AND cp.TIPO = dcp.TIPO_CPTE
	AND cp.NUMERO = dcp.COMPROBANTE
INNER JOIN COMPANIA c 
    ON 	cp.COMPANIA = c.CODIGO
INNER JOIN PLAN_PRESUPUESTAL pp 
    ON 	dcp.COMPANIA = pp.COMPANIA
	AND dcp.ANO = pp.ANO
	AND dcp.CUENTA = pp.CODIGO 
LEFT JOIN FUENTE_RECURSOS fr
    ON 	dcp.COMPANIA = fr.COMPANIA
	AND dcp.ANO = fr.ANO
	AND CASE WHEN pp.MAN_AUX_FUE =-1 THEN dcp.FUENTE_RECURSO ELSE pp.FUENTE_RECURSOS END  = fr.CODIGO
INNER JOIN TIPO_COMPROBPP tc 
   ON 	cp.COMPANIA = tc.COMPANIA
	AND cp.TIPO = tc.CODIGO
LEFT JOIN TIPOCLASIFICADOR CODIGO_CCPET
  ON dcp.COMPANIA= CODIGO_CCPET.COMPANIA
  AND dcp.ANO= CODIGO_CCPET.ANO
  AND '006'= CODIGO_CCPET.CLASECLASIFICADOR
   AND dcp.CODIGO_CCPET= CODIGO_CCPET.CODIGO
LEFT JOIN TIPOVIGENCIA 
ON PP.TIPOVIGENCIA = TIPOVIGENCIA.CODIGO
WHERE
	cp.COMPANIA = s$compania$s
    and cp.ANO=s$ano$s 
	AND cp.MES  BETWEEN s$mesInicial$s AND s$mesFinal$s
	AND tc.CLASE IN ('ADD', 'DIS', 'DMD')
    AND pp.TIPOVIGENCIA IN (s$vigenciasAppui$s)
    AND pp.REGALIAS IN (0)
   ORDER BY  dcp.FECHA)
   ]') CONSULTA, 99 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);