WITH SALDOSMESDETALLE AS( 
    SELECT  COMPANIA, 
            ANO, 
            ID,
            CODIGO, 
            MES,
            SUM(APROPIADO               ) APROPIADO,
            SUM(APROPIACIONVIGENTE      ) APROPIACIONVIGENTE,
            SUM(COMPROMISOSACUM         ) COMPROMISOSACUM
    FROM V_RESUMENPPTO_BASE
    WHERE COMPANIA =  s$compania$s
      AND ANO      =  s$ano$s
      AND ID       BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
    GROUP BY  COMPANIA, 
              ANO, 
              ID, 
              CODIGO,
              MES)      
SELECT DISTINCT 
  PLAN_PRESUPUESTAL.ID,
  PLAN_PRESUPUESTAL.CODIGO,
  PLAN_PRESUPUESTAL.NOMBRE,
  DETALLE_COMPROBANTE_PPTAL.MES,
  DETALLE_COMPROBANTE_PPTAL.DIA,
  DETALLE_COMPROBANTE_PPTAL.ANO,
  PLAN_PRESUPUESTAL.VIGENCIA,
  DETALLE_COMPROBANTE_PPTAL.FECHA,
  CASE WHEN TIPO_VF = '01' THEN 'ORDINARIA'ELSE 
      CASE WHEN TIPO_VF = '02' THEN 'EXCEPCIONAL' ELSE 'DE IMPORTANCIA ESTRATĂ‰GICA' END
  END AS TIPO_VFT,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
  CASE WHEN CASE WHEN TIPO_COMPROBPP.CLASE ='VAC' THEN '10' ELSE '' || 
        CASE WHEN TIPO_COMPROBPP.CLASE ='APR' THEN '12' ELSE '' ||
            CASE WHEN TIPO_COMPROBPP.CLASE = 'RES' OR TIPO_COMPROBPP.CLASE ='DMR' OR TIPO_COMPROBPP.CLASE ='ADR' THEN '13' ELSE '' END
        END
    END = '10' THEN 'VA'ELSE '' ||
      CASE WHEN CASE WHEN TIPO_COMPROBPP.CLASE ='VAC' THEN '10' ELSE '' || 
          CASE WHEN TIPO_COMPROBPP.CLASE ='APR' THEN '12'ELSE '' ||
            CASE WHEN TIPO_COMPROBPP.CLASE = 'RES' OR TIPO_COMPROBPP.CLASE ='DMR' OR TIPO_COMPROBPP.CLASE ='ADR'THEN '13' ELSE '' END
          END
        END ='12' THEN 'VFA' ELSE '' ||
          CASE WHEN CASE WHEN TIPO_COMPROBPP.CLASE ='VAC' THEN '10' ELSE '' || 
              CASE WHEN TIPO_COMPROBPP.CLASE ='APR' THEN '12' ELSE '' ||
                CASE WHEN TIPO_COMPROBPP.CLASE = 'RES' OR TIPO_COMPROBPP.CLASE ='DMR' OR TIPO_COMPROBPP.CLASE ='ADR' THEN '13' ELSE '' END
              END
            END ='13' THEN 'COMI' ELSE ''
          END
      END
  END AS CLASELETRAS,
  CASE WHEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) <0 THEN '2' ELSE '1' END  AS SIGNO,
  ABS(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) AS VALOR,
  COMPROBANTE_PPTAL.FECHA_APROB_VF,
  COMPROBANTE_PPTAL.TIPO_DOCUMENTO AS TIPO_APROBACION,
  COMPROBANTE_PPTAL.NRO_DOCUMENTO  AS NRO_APROBACION,
  COMPROBANTE_PPTAL.FECHA_AUTO_VF,
  COMPROBANTE_PPTAL.TIPO_AUTORIZACION_VF,
  COMPROBANTE_PPTAL.NRO_AUTORIZACION_VF,
  0              AS ANTERIORAUTORIZADO,
  DETALLE_COMPROBANTE_PPTAL.DESCRIPCION,
  TERCERO.NIT,
  TERCERO.NOMBRE AS NOMBRETERCERO,
  PLAN_PRESUPUESTAL.DESTINO,
  PLAN_SCHIP.CUENTA,
  PLAN_SCHIP.SUBCUENTA,
  PLAN_SCHIP.OBJETO_DE_GASTO,
  PLAN_SCHIP.ORDINAL,
  PLAN_SCHIP.SUBORDINAL,
  PLAN_SCHIP.PROGRAMA,
  PLAN_SCHIP.SUBPROGRAMA,
  PLAN_SCHIP.PROYECTO,
  PLAN_SCHIP.SUBPROYECTO,
  PLAN_PRESUPUESTAL.RECURSOSCHIP,
  PLAN_PRESUPUESTAL.FINALIDADGASTO,
  PLAN_PRESUPUESTAL.CODIGOSCHIP,
  COMPANIA.CODIGOSCHIP AS COMPACODIGOSCHIP,
  SALDOAPROPIACIONVIGENTE.APROPIADO NUEVOVALORAUTORIZADO,
  SALDOSMESDETALLE.COMPROMISOSACUM VALORANTERIORCOMPROMETIDO,
  SALDOAPROPIADO.APROPIADO INICIAL
FROM COMPROBANTE_PPTAL
  INNER JOIN TERCERO
    ON TERCERO.COMPANIA                   = COMPROBANTE_PPTAL.COMPANIA 
    AND TERCERO.NIT                        = COMPROBANTE_PPTAL.TERCERO
    AND TERCERO.SUCURSAL                    = COMPROBANTE_PPTAL.SUCURSAL
  INNER JOIN DETALLE_COMPROBANTE_PPTAL
    ON COMPROBANTE_PPTAL.COMPANIA          = DETALLE_COMPROBANTE_PPTAL.COMPANIA
    AND COMPROBANTE_PPTAL.ANO              = DETALLE_COMPROBANTE_PPTAL.ANO
    AND COMPROBANTE_PPTAL.TIPO             = DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE
    AND COMPROBANTE_PPTAL.NUMERO           = DETALLE_COMPROBANTE_PPTAL.COMPROBANTE
  INNER JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA 
    AND DETALLE_COMPROBANTE_PPTAL.ANO      = PLAN_PRESUPUESTAL.ANO
    AND DETALLE_COMPROBANTE_PPTAL.ID    = PLAN_PRESUPUESTAL.ID
  INNER JOIN TIPO_COMPROBPP
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA   = TIPO_COMPROBPP.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
  INNER JOIN PLAN_SCHIP
    ON PLAN_PRESUPUESTAL.CODIGOSCHIP = PLAN_SCHIP.CODIGO
  INNER JOIN COMPANIA 
    ON PLAN_PRESUPUESTAL.COMPANIA = COMPANIA.CODIGO
  INNER JOIN (  SELECT  COMPANIA, 
            ANO, 
            ID,
            CODIGO, 
            SUM(APROPIADO               ) APROPIADO
    FROM V_RESUMENPPTO_BASE
    WHERE COMPANIA =  s$compania$s
      AND ANO      =  s$ano$s
      AND MES      BETWEEN s$mesInicial$s AND s$mesFinal$s 
      AND ID       BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
    GROUP BY  COMPANIA, 
              ANO, 
              ID, 
              CODIGO) SALDOAPROPIACIONVIGENTE 
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = SALDOAPROPIACIONVIGENTE.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO = SALDOAPROPIACIONVIGENTE.ANO
    AND DETALLE_COMPROBANTE_PPTAL.ID = SALDOAPROPIACIONVIGENTE.ID
  INNER JOIN SALDOSMESDETALLE 
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = SALDOSMESDETALLE.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO = SALDOSMESDETALLE.ANO
    AND DETALLE_COMPROBANTE_PPTAL.ID = SALDOSMESDETALLE.ID
    AND DETALLE_COMPROBANTE_PPTAL.MES - 1 = SALDOSMESDETALLE.MES
  INNER JOIN SALDOSMESDETALLE SALDOAPROPIADO
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = SALDOAPROPIADO.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO = SALDOAPROPIADO.ANO
    AND DETALLE_COMPROBANTE_PPTAL.ID = SALDOAPROPIADO.ID
    AND DETALLE_COMPROBANTE_PPTAL.MES = SALDOAPROPIADO.MES 
WHERE COMPROBANTE_PPTAL.COMPANIA = s$compania$s
  AND DETALLE_COMPROBANTE_PPTAL.ANO  = s$ano$s
  AND DETALLE_COMPROBANTE_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s
  AND PLAN_PRESUPUESTAL.ID BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
  AND PLAN_PRESUPUESTAL.TIPOVIGENCIA ='VF'
  AND  CASE WHEN CASE WHEN TIPO_COMPROBPP.CLASE ='VAC' THEN '10' ELSE '' || 
        CASE WHEN TIPO_COMPROBPP.CLASE ='APR' THEN '12' ELSE '' ||
            CASE WHEN TIPO_COMPROBPP.CLASE = 'RES' OR TIPO_COMPROBPP.CLASE ='DMR' OR TIPO_COMPROBPP.CLASE ='ADR' THEN '13' ELSE '' END
        END
    END = '10' THEN 'VA'ELSE '' ||
      CASE WHEN CASE WHEN TIPO_COMPROBPP.CLASE ='VAC' THEN '10' ELSE '' || 
          CASE WHEN TIPO_COMPROBPP.CLASE ='APR' THEN '12'ELSE '' ||
            CASE WHEN TIPO_COMPROBPP.CLASE = 'RES' OR TIPO_COMPROBPP.CLASE ='DMR' OR TIPO_COMPROBPP.CLASE ='ADR'THEN '13' ELSE '' END
          END
        END ='12' THEN 'VFA' ELSE '' ||
          CASE WHEN CASE WHEN TIPO_COMPROBPP.CLASE ='VAC' THEN '10' ELSE '' || 
              CASE WHEN TIPO_COMPROBPP.CLASE ='APR' THEN '12' ELSE '' ||
                CASE WHEN TIPO_COMPROBPP.CLASE = 'RES' OR TIPO_COMPROBPP.CLASE ='DMR' OR TIPO_COMPROBPP.CLASE ='ADR' THEN '13' ELSE '' END
              END
            END ='13' THEN 'COMI' ELSE ''
          END
      END
  END IS NOT NULL
ORDER BY PLAN_PRESUPUESTAL.ID,
  DETALLE_COMPROBANTE_PPTAL.FECHA DESC
