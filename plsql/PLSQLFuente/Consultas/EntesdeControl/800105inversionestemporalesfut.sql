SELECT FUENTE_RECURSOS.CODIGO_FUT AS CONCEPTO,
INVERSIONRF.CODIGO AS NUMERO,
INVERSIONRF.TIPO_INV_FUT AS TIPO_INVERSION,
INVERSIONRF.MONEDA,
INVERSIONRF.ENTIDADEMISORA AS ENTIDAD,
INVERSIONRF.INTERMEDIARIA AS ENTIDAD_INTERMEDIARIA,
INVERSIONRF.PLAZO,
INVERSIONRF.TASA AS TASA_CUPON,
TO_CHAR(INVERSIONRF.FECHACOLOCACION,'DD-MM-YYYY') AS FECHA_EMISION,
TO_CHAR(INVERSIONRF.FECHA_COMPRA,'DD-MM-YYYY') AS FECHA_COMP,
CASE WHEN INVERSIONRF.VALORNOMINAL<>0
		THEN ROUND(INVERSIONRF.VALORNOMINAL s$pesos$s ,s$redondeo$s)
		ELSE 0
END AS VALOR_NOMINAL,
INVERSIONRF.TASA_COMPRA,
CASE WHEN NVL(INVERSIONRF.VALOR_PAGADO,0)s$pesos$s <>0
		THEN ROUND(INVERSIONRF.VALOR_PAGADO s$pesos$s,s$redondeo$s)
		ELSE 0
END AS VALOR_PAGA,
TO_CHAR(INVERSIONRF.FECHAVENCIMIENTO,'DD-MM-YYYY') AS FECHA_VENCIMIENTO,
CASE WHEN INVERSION_VENDIDA=-1
		THEN 'TRUE'
		ELSE 'FALSE'
END AS INVERSION_VENDID,
CASE WHEN NVL(INVERSIONRF.VALOR_VENTA,0)s$pesos$s <>0
		THEN ROUND(INVERSIONRF.VALOR_VENTA s$pesos$s ,s$redondeo$s)
		ELSE 0
END AS VALOR_VNT,
CASE WHEN NVL(INVERSIONRF.FECHA_VENTA,'')=''
		THEN TO_CHAR(INVERSIONRF.FECHAVENCIMIENTO,'dd-mm-yyyy')
		ELSE TO_CHAR(INVERSIONRF.FECHA_VENTA,'dd-mm-yyyy')
END AS FECHA_VNT,
NVL(INVERSIONRF.TASA_VENTA,0) AS TASA_VENTA,
INVERSIONRF.RENDIMIENTOSFINANCIEROS as TASA_RENDIMIENTO_VENCIMIENTO,
CASE WHEN INVERSIONRF.TASA<>0
		THEN ROUND((CASE WHEN INVERSIONRF.VALORNOMINAL<>0
						THEN ROUND(INVERSIONRF.VALORNOMINAL s$pesos$s ,s$redondeo$s)
						ELSE 0
					END )/ INVERSIONRF.TASA,s$redondeo$s)
		ELSE 0
END AS RENDIMIENTOS_DETALLE
FROM INVERSIONRF 
	INNER JOIN FUENTE_RECURSOS 
		ON INVERSIONRF.COMPANIA = FUENTE_RECURSOS.COMPANIA
		AND INVERSIONRF.ORIGENRECURSOS = FUENTE_RECURSOS.CODIGO
WHERE INVERSIONRF.FECHAVENCIMIENTO >= s$fechaCorte$s AND FUENTE_RECURSOS.compania='s$compania$s' AND FUENTE_RECURSOS.ANO = s$ano$s
