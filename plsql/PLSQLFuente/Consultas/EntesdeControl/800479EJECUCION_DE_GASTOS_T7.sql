           ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = FUENTE_CUIPO.COMPANIA
           AND DETALLE_COMPROBANTE_PPTAL.ANO = FUENTE_CUIPO.ANO
           AND '009'=FUENTE_CUIPO.CLASECLASIFICADOR
           AND DETALLE_COMPROBANTE_PPTAL.FUENTE_CUIPO = FUENTE_CUIPO.CO]') || TO_CLOB(q'[DIGO
      LEFT JOIN TIPOCLASIFICADOR COD_CPC_CUIPO
           ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = COD_CPC_CUIPO.COMPANIA
           AND DETALLE_COMPROBANTE_PPTAL.ANO = COD_CPC_CUIPO.ANO
           AND '007'=COD_CPC_CUIPO.CLASECLASIFICADOR
           AND DETALLE_COMPROBANTE_PPTAL.CODIGO_CPC = COD_CPC_CUIPO.CODIGO    
      LEFT JOIN TIPOCLASIFICADOR SECCION_PPTAL_CUIPO
           ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = SECCION_PPTAL_CUIPO.COMPANIA
           AND DETALLE_COMPROBANTE_PPTAL.ANO = SECCION_PPTAL_CUIPO.ANO
           AND '008'= SECCION_PPTAL_CUIPO.CLASECLASIFICADOR
           AND DETALLE_COMPROBANTE_PPTAL.CODIGOUNIDADEJE  =  SECCION_PPTAL_CUIPO.CODIGO
      LEFT JOIN TIPOCLASIFICADOR COD_PRODUCTO_CUIPO
           ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = COD_PRODUCTO_CUIPO.COMPANIA
           AND DETALLE_COMPROBANTE_PPTAL.ANO = COD_PRODUCTO_CUIPO.ANO
           AND '004'=COD_PRODUCTO_CUIPO.CLASECLASIFICADOR
           AND DETALLE_COMPROBANTE_PPTAL.COD_PROD_CUIPO = COD_PRODUCTO_CUIPO.CODIGO  
     LEFT JOIN TIPOCLASIFICADOR SECTOR_CUIPO
           ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = SECTOR_CUIPO.COMPANIA
           AND DETALLE_COMPROBANTE_PPTAL.ANO = SECTOR_CUIPO.ANO
           AND '001'= SECTOR_CUIPO.CLASECLASIFICADOR
           AND DETALLE_COMPROBANTE_PPTAL.SECTOR   =  SECTOR_CUIPO.CODIGO 
  INNER JOIN TIPO_COMPROBPP
  ON DETALLE_COMPROBANTE_PPTAL.COMPANIA   = TIPO_COMPROBPP.COMPANIA
  AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
  INNER JOIN TERCERO
  ON DETALLE_COMPROBANTE_PPTAL.COMPANIA  = TERCERO.COMPANIA
  AND DETALLE_COMPROBANTE_PPTAL.TERCERO  = TERCERO.NIT
  AND DETALLE_COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL
  WHERE P.COMPANIA                   = s$compania$s
  AND P.ANO                          = s$anio$s
  AND DETALLE_COMPROBANTE_PPTAL.MES <= s$mesfinal$s
  AND DETALLE_COMPROBANTE_PPTAL.NATURALEZA                   ='D'
  AND P.REGALIAS                    IN(0)
  GROUP BY CUENTA,
    CODIGO_CCEPT.COMPANIA,
    CODIGO_CCEPT.ANO,
    CODIGO_CCEPT.CODIGO,
    CODIGO_CCEPT.NOMBRE,
    P.TIPOVIGENCIA,
    SECCION_PPTAL_CUIPO.CODIGO,
    P.SECTOR,
    DETALLE_COMPROBANTE_PPTAL.PROGRAMA,
    DETALLE_COMPROBANTE_PPTAL.CODIGO_BPIN,
    NVL(DETALLE_COMPROBANTE_PPTAL.CODIGO_CPC,'0'),
    DETALLE_COMPROBANTE_PPTAL.CODIGO_CPC,
    '0.0',
    DETALLE_COMPROBANTE_PPTAL.PROGRAMA,
    FUENTE_CUIPO.CODIGO,
    '0.0',
    CASE
      WHEN P.APLICA_DEST_ESPECIFICA NOT IN (0)
      THEN P.TIPO_NORMA
      ELSE '0'
    END,
    P.APLICA_DEST_ESPECIFICA,
    0,
    P.TIPO_NORMA,
    '0.0',
    CASE
      WHEN P.APLICA_DEST_ESPECIFICA NOT IN (0)
      THEN P.NUMERO_NORMA
      ELSE '0.0'
    END,
    P.APLICA_DEST_ESPECIFICA,
    0,
    P.NUMERO_NORMA,
    '0.0',
    CASE
      WHEN P.APLICA_DEST_ESPECIFICA NOT IN (0)
      THEN TO_CHAR(P.FECHA_NORMA,'DD/MM/YYYY')
      ELSE '01/01/1900'
    END,
    P.APLICA_DEST_ESPECIFICA,
    0,
    TO_CHAR(P.FECHA_NORMA,'DD/MM/YYYY'),
    P.FECHA_NORMA,
    'DD/MM/YYYY',
    ']') || TO_CLOB(q'[01/01/1900',
    '',
    '',
    CASE
      WHEN P.CONSITUACIONFONDOS IN (0)
      THEN 'C'
      ELSE 'S'
    END ,
    P.APLICA_DEST_ESPECIFICA,
    0,
    '1.0',
    '2.0',
    NVL(P.POLITCA_PUBLICA_CUIPO,'0'),
    P.POLITCA_PUBLICA_CUIPO,
    '0',
    CASE
      WHEN P.TRANSFERENCIA NOT IN (0)
