      THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)
      ELSE 0
    END) AS OBLIGACIONES,
    SUM(
    CASE
      WHEN TIPO_COMPROBPP.CLASE                   IN ('EGR','ADG','DEG')
      THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)
      ELSE 0
    END) AS PAGOS
  FROM DETALLE_COMPROBANTE_PPTAL  INNER JOIN PLAN_PRESUPUESTAL P
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = P.COMPANIA
	     AND DETALLE_COMPROBANTE_PPTAL .ANO = P.ANO
        AND DETALLE_COMPROBANTE_PPTAL .CUENTA = P.CODIGO
      INNER JOIN TIPOCLASIFICADOR CODIGO_CCEPT
           ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = CODIGO_CCEPT.COMPANIA
           AND DETALLE_COMPROBANTE_PPTAL.ANO = CODIGO_CCEPT.ANO
