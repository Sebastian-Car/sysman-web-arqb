  ) CUIPO
WHERE (COMPROMISOS + OBLIGACIONES + PAGOS) NOT IN(0)
 GROUP BY CODIGO,
  --s$nombre$s
  VIGENCIA,
  NVL(SECCION_PPTAL_CUIPO,0),
  NVL(SECTOR,0) ,
  NVL(CPC,0) ,
