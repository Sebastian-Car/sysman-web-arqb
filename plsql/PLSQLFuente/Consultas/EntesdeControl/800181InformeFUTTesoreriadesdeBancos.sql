             THEN ROUND(VALOR_CREDITO,0)
             ELSE ROUND(VALOR_DEBITO ,0) 
             END ), s$redondeo$s) VALOR 
FROM DETALLE_COMPROBANTE_CNT INNER JOIN CUENTABANCOS 
  ON DETALLE_COMPROBANTE_CNT.COMPANIA = CUENTABANCOS.COMPANIA 
 AND DETALLE_COMPROBANTE_CNT.ANO      = CUENTABANCOS.ANO 
 AND DETALLE_COMPROBANTE_CNT.CUENTA   = CUENTABANCOS.IDCONTABLE 
INNER JOIN PLAN_CONTABLE 
   ON DETALLE_COMPROBANTE_CNT.COMPANIA   = PLAN_CONTABLE.COMPANIA 
  AND DETALLE_COMPROBANTE_CNT.ANO        = PLAN_CONTABLE.ANO 
  AND DETALLE_COMPROBANTE_CNT.CUENTA     = PLAN_CONTABLE.CODIGO   
