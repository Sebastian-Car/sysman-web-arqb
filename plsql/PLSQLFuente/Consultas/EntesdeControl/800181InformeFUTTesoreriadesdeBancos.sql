SELECT
    'ITFS' CODIGO_FUT,
    ROUND(SUM(CASE WHEN TIPO_SALUD='RS'  
             THEN SALDOs$mesanterior$s  
             ELSE 0 END), s$redondeo$s)  REGIMEN_SUBSIDIADO, 
    ROUND(SUM(CASE WHEN TIPO_SALUD ='SPC' 
             THEN SALDOs$mesanterior$s  
             ELSE 0 END), s$redondeo$s)  SALUD_PUBLICA, 
    ROUND(SUM(CASE WHEN TIPO_SALUD='PSO' 
             THEN SALDOs$mesanterior$s  
             ELSE 0 END), s$redondeo$s)  PRESTACION_DEL_SERVICIO_OFERTA, 
    ROUND(SUM(CASE WHEN TIPO_SALUD ='OGSI'
             THEN SALDOs$mesanterior$s  
             ELSE 0 END), s$redondeo$s)  OTROS_GASTOS_SALUD_INVERSION, 
    ROUND(SUM(CASE WHEN TIPO_SALUD='OGSF'
             THEN SALDOs$mesanterior$s  
             ELSE 0 END), s$redondeo$s)  OTROS_GASTOS_SALUD_FUN,
    0 SALDO_PENDIENTE_POR_TRANSFERIR,
    ROUND(SUM(SALDOs$mesanterior$s), s$redondeo$s) VALOR
FROM PLAN_CONTABLE INNER JOIN CUENTABANCOS 
  ON PLAN_CONTABLE.COMPANIA = CUENTABANCOS.COMPANIA 
 AND PLAN_CONTABLE.ANO      = CUENTABANCOS.ANO 
 AND PLAN_CONTABLE.CODIGO   = CUENTABANCOS.IDCONTABLE 
WHERE PLAN_CONTABLE.COMPANIA  = s$compania$s
  AND PLAN_CONTABLE.CUENTAS_MAESTRAS_SALUD NOT IN(0)
  AND PLAN_CONTABLE.ANO       = s$anioTrabajo$s 
  AND CUENTABANCOS.ESTADO IN('A')
  AND CUENTABANCOS.ESTADO IS NOT NULL
GROUP BY 'FTFS',
    PLAN_CONTABLE.COMPANIA,
    PLAN_CONTABLE.ANO

UNION

SELECT CODIGO_FUT,  
                   SUM(REGIMEN_SUBSIDIADO), 
                   SUM(SALUD_PUBLICA), 
                   SUM(PRESTACION_DEL_SERVICIO_OFERTA),
                   SUM(OTROS_GASTOS_SALUD_INVERSION),
                   SUM(OTROS_GASTOS_SALUD_FUN),
                   SUM(SALDO_PENDIENTE_POR_TRANSFERIR),
                   SUM(VALOR)
FROM (
SELECT
    'FTFS' CODIGO_FUT,
    ROUND(CASE WHEN TIPO_SALUD='RS' THEN SALDOs$mesanterior$s  ELSE 0 END , s$redondeo$s) +         
    ROUND(SUM(CASE WHEN TIPO_SALUD='RS'  THEN CASE WHEN CODIGOS_FUT_FORMULARIO.NATURALEZA='D' 
             THEN ROUND(VALOR_CREDITO,0)
             ELSE ROUND(VALOR_DEBITO ,0) 
             END ELSE 0 END *(CASE WHEN SUBSTR(DETALLE_COMPROBANTE_CNT.CODIGO_FUT, 1, 4) ='PTFS' THEN -1 ELSE 1 END)  ), s$redondeo$s)  REGIMEN_SUBSIDIADO, 
    ROUND(CASE WHEN TIPO_SALUD='SPC' THEN SALDOs$mesanterior$s  ELSE 0 END , s$redondeo$s)+  
    ROUND(SUM(CASE WHEN TIPO_SALUD='SPC'  THEN CASE WHEN CODIGOS_FUT_FORMULARIO.NATURALEZA='D' 
             THEN ROUND(VALOR_CREDITO,0)
             ELSE ROUND(VALOR_DEBITO ,0) 
             END ELSE 0 END *(CASE WHEN SUBSTR(DETALLE_COMPROBANTE_CNT.CODIGO_FUT, 1, 4) ='PTFS' THEN -1 ELSE 1 END) ), s$redondeo$s)  SALUD_PUBLICA, 
    ROUND(CASE WHEN TIPO_SALUD='PSO' THEN SALDOs$mesanterior$s  ELSE 0 END , s$redondeo$s) +           
    ROUND(SUM(CASE WHEN TIPO_SALUD='PSO'  THEN CASE WHEN CODIGOS_FUT_FORMULARIO.NATURALEZA='D' 
             THEN ROUND(VALOR_CREDITO,0)
             ELSE ROUND(VALOR_DEBITO ,0) 
             END ELSE 0 END *(CASE WHEN SUBSTR(DETALLE_COMPROBANTE_CNT.CODIGO_FUT, 1, 4) ='PTFS' THEN -1 ELSE 1 END) ), s$redondeo$s)  PRESTACION_DEL_SERVICIO_OFERTA, 
    ROUND(CASE WHEN TIPO_SALUD='OGSI' THEN SALDOs$mesanterior$s  ELSE 0 END , s$redondeo$s) +           
    ROUND(SUM(CASE WHEN TIPO_SALUD='OGSI' THEN CASE WHEN CODIGOS_FUT_FORMULARIO.NATURALEZA='D' 
             THEN ROUND(VALOR_CREDITO,0)
             ELSE ROUND(VALOR_DEBITO ,0) 
             END ELSE 0 END *(CASE WHEN SUBSTR(DETALLE_COMPROBANTE_CNT.CODIGO_FUT, 1, 4) ='PTFS' THEN -1 ELSE 1 END) ) , s$redondeo$s) OTROS_GASTOS_SALUD_INVERSION, 
    ROUND(CASE WHEN TIPO_SALUD='OGSF' THEN SALDOs$mesanterior$s  ELSE 0 END , s$redondeo$s)+
    ROUND(SUM(CASE WHEN TIPO_SALUD='OGSF'  THEN CASE WHEN CODIGOS_FUT_FORMULARIO.NATURALEZA='D' 
             THEN ROUND(VALOR_CREDITO,0)
             ELSE ROUND(VALOR_DEBITO ,0) 
             END ELSE 0 END *(CASE WHEN SUBSTR(DETALLE_COMPROBANTE_CNT.CODIGO_FUT, 1, 4) ='PTFS' THEN -1 ELSE 1 END) ) , s$redondeo$s) OTROS_GASTOS_SALUD_FUN,
     0 SALDO_PENDIENTE_POR_TRANSFERIR,
     ROUND(SALDOs$mesanterior$s , s$redondeo$s) +
    ROUND(SUM(CASE WHEN CODIGOS_FUT_FORMULARIO.NATURALEZA='D' 
             THEN ROUND(VALOR_CREDITO,0)
             ELSE ROUND(VALOR_DEBITO ,0) 
             END *(CASE WHEN SUBSTR(DETALLE_COMPROBANTE_CNT.CODIGO_FUT, 1, 4) ='PTFS' THEN -1 ELSE 1 END)  ), s$redondeo$s) VALOR 
FROM DETALLE_COMPROBANTE_CNT INNER JOIN CUENTABANCOS 
  ON DETALLE_COMPROBANTE_CNT.COMPANIA = CUENTABANCOS.COMPANIA 
 AND DETALLE_COMPROBANTE_CNT.ANO      = CUENTABANCOS.ANO 
 AND DETALLE_COMPROBANTE_CNT.CUENTA   = CUENTABANCOS.IDCONTABLE 
INNER JOIN PLAN_CONTABLE 
   ON DETALLE_COMPROBANTE_CNT.COMPANIA   = PLAN_CONTABLE.COMPANIA 
  AND DETALLE_COMPROBANTE_CNT.ANO        = PLAN_CONTABLE.ANO 
  AND DETALLE_COMPROBANTE_CNT.CUENTA     = PLAN_CONTABLE.CODIGO   
INNER JOIN CODIGOS_FUT_FORMULARIO 
   ON DETALLE_COMPROBANTE_CNT.COMPANIA   = CODIGOS_FUT_FORMULARIO.COMPANIA 
  AND DETALLE_COMPROBANTE_CNT.ANO        = CODIGOS_FUT_FORMULARIO.ANO 
  AND DETALLE_COMPROBANTE_CNT.CODIGO_FUT = CODIGOS_FUT_FORMULARIO.CODIGO    
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA   = s$compania$s
  AND DETALLE_COMPROBANTE_CNT.CODIGO_FUT IS NOT NULL
  AND DETALLE_COMPROBANTE_CNT.ANO        = s$anioTrabajo$s 
  AND DETALLE_COMPROBANTE_CNT.MES        BETWEEN s$mesinicial$s AND s$mesfinal$s
  AND CUENTABANCOS.ESTADO                IN ('A')
  AND PLAN_CONTABLE.CUENTAS_MAESTRAS_SALUD NOT IN(0)
GROUP BY 'FTFS',
    DETALLE_COMPROBANTE_CNT.COMPANIA,
    DETALLE_COMPROBANTE_CNT.ANO,    
    SALDOs$mesanterior$s,
    CASE WHEN TIPO_SALUD='RS' THEN SALDOs$mesanterior$s  ELSE 0 END,
    CASE WHEN TIPO_SALUD='SPC' THEN SALDOs$mesanterior$s  ELSE 0 END,
    CASE WHEN TIPO_SALUD='PSO' THEN SALDOs$mesanterior$s  ELSE 0 END,
    CASE WHEN TIPO_SALUD='OGSI' THEN SALDOs$mesanterior$s  ELSE 0 END,
    CASE WHEN TIPO_SALUD='OGSF' THEN SALDOs$mesanterior$s  ELSE 0 END            
)
GROUP BY CODIGO_FUT
    
UNION

SELECT
