   ON DETALLE_COMPROBANTE_CNT.COMPANIA   = CODIGOS_FUT_FORMULARIO.COMPANIA 
  AND DETALLE_COMPROBANTE_CNT.ANO        = CODIGOS_FUT_FORMULARIO.ANO 
  AND DETALLE_COMPROBANTE_CNT.CODIGO_FUT = CODIGOS_FUT_FORMULARIO.CODIGO    
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA   = s$compania$s
  AND DETALLE_COMPROBANTE_CNT.CODIGO_FUT IS NOT NULL
  AND DETALLE_COMPROBANTE_CNT.ANO        = s$anioTrabajo$s 
  AND DETALLE_COMPROBANTE_CNT.MES        BETWEEN s$mesinicial$s AND s$mesfinal$s
