SELECT TO_CHAR(CUENTABANCOS.ANO) ANO,
      CUENTABANCOS.IDCONTABLE CUENTA,
      CUENTABANCOS.BANCO      CODIGO_BANCO,
      UPPER(CUENTABANCOS.SUCURSALBANCO) SUCURSALBANCO,
      UPPER(BANCO.NOMBREBANCO) NOMBREBANCO,
      TO_CHAR(CUENTABANCOS.FECHAAPERTURA, 'DD/MM/YYYY') FECHAAPERTURA,
      TO_CHAR(CUENTABANCOS.FECHACANCELACION, 'DD/MM/YYYY') FECHACANCELACION,
      UPPER(CUENTABANCOS.TIPOCUENTA) TIPOCUENTA,
      CUENTABANCOS.DIRECCION,
      CUENTABANCOS.CUENTANUMERO,
      UPPER(CLASECUENTABANCO.NOMBRE) CLASE_CUENTA,
      CUENTABANCOS.RECURSOS,
      CASE WHEN CUENTABANCOS.ESTADO = 'A'
           THEN 'ACTIVA'
           ELSE 'CANCELADA'
      END ESTADO,      
      CUENTABANCOS.GERENTE,
      CASE WHEN CUENTABANCOS.ESOFICIAL IN (-1)
           THEN 'OFICIAL'
           ELSE 'NO OFICIAL'
      END OFICIAL     
FROM CUENTABANCOS
  INNER JOIN BANCO
     ON CUENTABANCOS.COMPANIA    = BANCO.COMPANIA
    AND CUENTABANCOS.BANCO       = BANCO.BANCO
  INNER JOIN CLASECUENTABANCO
     ON CUENTABANCOS.CLASECUENTA = CLASECUENTABANCO.CODIGO
WHERE CUENTABANCOS.COMPANIA = s$compania$s
  AND CUENTABANCOS.ANO      = s$anio$s
