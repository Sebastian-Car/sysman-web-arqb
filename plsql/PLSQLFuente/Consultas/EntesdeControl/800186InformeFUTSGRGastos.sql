SELECT V_PP.CODIGOFUT_REGALIAS      CONCEPTO,
        NVL(V_PP.CODIGO_BPIN_SGR,0) CODIGO_BPIN_SGR,
        V_PP.FUENTE_FUTREGALIAS     FUENTES_FINANCIACION_SGR,
        V_PP.SECTOR_REGALIAS        SECTOR,
        MIN(V_PP.NOMBRE)             NOMBRE_DEL_PROYECTO,
        SUM(V_RESUMENPPTO_BASE.APROPIADO + 
            V_RESUMENPPTO_BASE.ADICION   + 
            V_RESUMENPPTO_BASE.REDUCCION + 
            V_RESUMENPPTO_BASE.TRASLADO) APROPIACIONES, 
        SUM(V_RESUMENPPTO_BASE.REG_CONTRACT         
          + V_RESUMENPPTO_BASE.REG_NO_CONTRACT) COMPROMISOS_SGR, 
         SUM(V_RESUMENPPTO_BASE.REO + V_RESUMENPPTO_BASE.MODIFREO) OBLIGACIONES, 
         SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT - V_RESUMENPPTO_BASE.REINTEGRO) PAGOS
FROM V_PLAN_PRESUPUESTAL V_PP
 INNER JOIN V_RESUMENPPTO_BASE
   ON V_PP.COMPANIA                 = V_RESUMENPPTO_BASE.COMPANIA
  AND V_PP.ANO                      = V_RESUMENPPTO_BASE.ANO
  AND V_PP.ID                       = V_RESUMENPPTO_BASE.ID
WHERE V_RESUMENPPTO_BASE.COMPANIA          = s$compania$s 
AND V_RESUMENPPTO_BASE.MES BETWEEN 0 AND  s$mesfinal$s
AND V_RESUMENPPTO_BASE.ANO                 = s$aniotrabajo$s
AND V_PP.CODIGOFUT_REGALIAS IS NOT NULL
AND V_PP.SECTOR_REGALIAS    IS NOT NULL
AND V_PP.FUENTE_FUTREGALIAS IS NOT NULL
AND V_PP.NATURALEZA          ='D'
AND V_RESUMENPPTO_BASE.TIPOVIGENCIA NOT IN ('RC','RA')
AND (V_RESUMENPPTO_BASE.APROPIADO  <>0  
  OR V_RESUMENPPTO_BASE.ADICION    <>0  
  OR V_RESUMENPPTO_BASE.REDUCCION  <>0
  OR V_RESUMENPPTO_BASE.TRASLADO    <>0 
  OR V_RESUMENPPTO_BASE.REG_CONTRACT <>0
  OR V_RESUMENPPTO_BASE.REG_NO_CONTRACT <>0
  OR V_RESUMENPPTO_BASE.REO <>0
  OR V_RESUMENPPTO_BASE.MODIFREO <>0
  OR V_RESUMENPPTO_BASE.EJECUCIONPPT <>0 
  OR V_RESUMENPPTO_BASE.REINTEGRO <>0)
GROUP BY V_PP.CODIGOFUT_REGALIAS,
        NVL(V_PP.CODIGO_BPIN_SGR,0),
        V_PP.FUENTE_FUTREGALIAS,
        V_PP.SECTOR_REGALIAS
ORDER BY BY V_PP.CODIGOFUT_REGALIAS,
        NVL(V_PP.CODIGO_BPIN_SGR,0),
        V_PP.FUENTE_FUTREGALIAS,
        V_PP.SECTOR_REGALIAS
