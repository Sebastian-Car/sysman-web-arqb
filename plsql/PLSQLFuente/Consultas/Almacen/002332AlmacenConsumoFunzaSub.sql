SELECT  D_MOVIMIENTO.COMPANIA,      
        D_MOVIMIENTO.TIPOMOVIMIENTO,        
        D_MOVIMIENTO.MOVIMIENTO,       
        SUM(D_MOVIMIENTO.VALORTOTAL) AS SUMADEVALORTOTAL,  
        LPAD(ELEMENTO,s$nivelGrupo$s) AS AGRUPACION,     
        SUM(D_MOVIMIENTO.VALORIVA) AS SUMADEVALORIVA,   
        SUM(D_MOVIMIENTO.VALORBASE) AS SUMADEVALORBASE
     
FROM D_MOVIMIENTO 
WHERE D_MOVIMIENTO.COMPANIA         = $P{PR_COMPANIA}    
    AND D_MOVIMIENTO.TIPOMOVIMIENTO = $P{PR_TIPOMOVIMIENTO} 
    AND D_MOVIMIENTO.MOVIMIENTO     = $P{PR_MOVIMIENTO} 
GROUP BY D_MOVIMIENTO.COMPANIA, D_MOVIMIENTO.TIPOMOVIMIENTO, D_MOVIMIENTO.MOVIMIENTO, LPAD(ELEMENTO,s$nivelGrupo$s)