WITH ULTIMOMOVIMIENTO AS (SELECT  D_MOVIMIENTO.COMPANIA, 
            D_MOVIMIENTO.ELEMENTO,D_MOVIMIENTO.SERIE,
            D_MOVIMIENTO.TIPOMOVIMIENTO,
          MAX(MOVIMIENTO.CLASE_BODEGA_ORIGEN) KEEP (DENSE_RANK LAST
ORDER BY
	TO_DATE( TO_CHAR(D_MOVIMIENTO.FECHA + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA, 'hh24'), 'HOUR' ) + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA, 'mi' ), 'MINUTE') + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA, 'ss' ), 'SECOND') , 'DD/MM/YYYY hh24:mi:ss') , 'DD/MM/YYYY hh24:mi:ss')) CLASE_BODEGA_ORIGEN,
	MAX(MOVIMIENTO.CLASE_BODEGA_DESTINO) KEEP (DENSE_RANK LAST
ORDER BY
	TO_DATE( TO_CHAR(D_MOVIMIENTO.FECHA + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA, 'hh24'), 'HOUR' ) + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA, 'mi' ), 'MINUTE') + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA, 'ss' ), 'SECOND') , 'DD/MM/YYYY hh24:mi:ss') , 'DD/MM/YYYY hh24:mi:ss')) CLASE_BODEGA_DESTINO,
	MAX(MOVIMIENTO.BODEGA_DESTINO) KEEP (DENSE_RANK LAST
ORDER BY
	TO_DATE( TO_CHAR(D_MOVIMIENTO.FECHA + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA, 'hh24'), 'HOUR' ) + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA, 'mi' ), 'MINUTE') + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA, 'ss' ), 'SECOND') , 'DD/MM/YYYY hh24:mi:ss') , 'DD/MM/YYYY hh24:mi:ss')) BODEGA_DESTINO,
	MAX(MOVIMIENTO.BODEGA_ORIGEN) KEEP (DENSE_RANK LAST
ORDER BY
	TO_DATE( TO_CHAR(D_MOVIMIENTO.FECHA + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA, 'hh24'), 'HOUR' ) + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA, 'mi' ), 'MINUTE') + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA, 'ss' ), 'SECOND') , 'DD/MM/YYYY hh24:mi:ss') , 'DD/MM/YYYY hh24:mi:ss')) BODEGA_ORIGEN,
MAX( TO_DATE( TO_CHAR(D_MOVIMIENTO.FECHA + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA, 'hh24'), 'HOUR' ) + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA, 'mi' ), 'MINUTE') + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA, 'ss' ), 'SECOND') , 'DD/MM/YYYY hh24:mi:ss') , 'DD/MM/YYYY hh24:mi:ss') ) KEEP (DENSE_RANK LAST
ORDER BY
	ROWNUM) FECHA_HORA,
            TIPOMOVIMIENTO.CLASE,
            BODEGA.NO_EXPLOTADOS,
	        BODEGA.MANTENIMIENTO,
            MOVIMIENTO.DESCRIPCION,
            MOVIMIENTO.ORFEO,
            MOVIMIENTO.FACTURANO,
            D_MOVIMIENTO.PLACAANTERIOR
      FROM D_MOVIMIENTO INNER JOIN MOVIMIENTO 
      ON D_MOVIMIENTO.COMPANIA = MOVIMIENTO.COMPANIA 
      AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO 
      AND D_MOVIMIENTO.MOVIMIENTO = MOVIMIENTO.NUMERO
      INNER JOIN TIPOMOVIMIENTO ON
	D_MOVIMIENTO.COMPANIA = TIPOMOVIMIENTO.COMPANIA
	AND D_MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
INNER JOIN BODEGA ON
	MOVIMIENTO.COMPANIA = BODEGA.COMPANIA
	AND
	CASE
		WHEN TIPOMOVIMIENTO.CLASE = 'S' THEN MOVIMIENTO.BODEGA_ORIGEN
		ELSE MOVIMIENTO.BODEGA_DESTINO
	END = BODEGA.CODIGO
      WHERE IND_REG NOT IN(0)
      GROUP BY D_MOVIMIENTO.COMPANIA, 
            D_MOVIMIENTO.ELEMENTO,D_MOVIMIENTO.SERIE,TIPOMOVIMIENTO.CLASE,D_MOVIMIENTO.TIPOMOVIMIENTO,BODEGA.NO_EXPLOTADOS,
	BODEGA.MANTENIMIENTO,MOVIMIENTO.DESCRIPCION,MOVIMIENTO.ORFEO,MOVIMIENTO.FACTURANO,D_MOVIMIENTO.PLACAANTERIOR)
SELECT DISTINCT DEVOLUTIVO.ELEMENTO "CODIGO ELEMENTO",
    DEVOLUTIVO.SERIE "PLACA MAJ",
    ULTIMOMOVIMIENTO.PLACAANTERIOR "PLACA ANTERIOR",
    DEVOLUTIVO.MARCA "SERIE",
    INVENTARIO.NOMBRELARGO "DESCRIPCION ELEMENTO",
    ESTADODEVOLUTIVO.DESCRIPCION ESTADO,
    DEPENDENCIA.CODIGO "CODIGO DEPENDENCIA",
    DEPENDENCIA.NOMBRE   DEPENDENCIA,
    TERCERO.NOMBRE RESPONSABLE,
    DEVOLUTIVO.NIIF_VIDA_UTIL "VIDA UTIL",
    DEVOLUTIVO.NIIF_VALOR_TOTAL "VALOR HISTORICO", 
    NVL(DEPRECIAR.NIIF_VLRDEPRECIACION,0) "DEPRECIACION MENSUAL",
    NVL(DEPRECIAR.NIIF_DEPACUMULADA,0)  "DEPRECIACION ACUMULADA",
    DEVOLUTIVO.AVALUO,
    DEVOLUTIVO.DETERIORO,
    NVL(DEVOLUTIVO.NIIF_VLRLIBROS,0) "VALORLIBROS",
    TO_CHAR(DEVOLUTIVO.FECHAADQUISICION, 'DD/MM/YYYY') AS "FECHA ADQUISICION",
    DEVOLUTIVO.TIPOMOVIMIENTOI "TIPO DE MOVIMIENTO",
    DEVOLUTIVO.MOVIMIENTOI "NUMERO MOVIMIENTO INGRESO",
    TO_CHAR(DEVOLUTIVO.FECHASALIDASERVICIO,'DD/MM/YYYY') AS "FECHA SALIDA SERVICIO",
    DEVOLUTIVO.TIPOMOVIMIENTOF "TIPO MOVIMIENTO SALIDA",
    DEVOLUTIVO.MOVIMIENTOF "NUMERO MOVIMIENTO SALIDA",
    ULTIMOMOVIMIENTO.FACTURANO No_FRA,
    TERCERO.NOMBRE PROVEEDOR,
    ULTIMOMOVIMIENTO.ORFEO No_ORFEO,
   CASE
		WHEN
		CASE
			WHEN ULTIMOMOVIMIENTO.CLASE = 'S' THEN ULTIMOMOVIMIENTO.CLASE_BODEGA_ORIGEN
			ELSE ULTIMOMOVIMIENTO.CLASE_BODEGA_DESTINO
		END IN('20') THEN
		CASE
			WHEN ULTIMOMOVIMIENTO.MANTENIMIENTO NOT IN(0) THEN NVL(ICUENTA.CUENTAACTIVOMAN, ICUENTA.CUENTAACTIVO)
			ELSE
			CASE
				WHEN ULTIMOMOVIMIENTO.NO_EXPLOTADOS NOT IN(0) THEN NVL(ICUENTA.CUENTAACTIVONEXP, ICUENTA.CUENTAACTIVO)
				ELSE ICUENTA.CUENTAACTIVO
			END
		END
		ELSE
		CASE
			WHEN
			CASE
				WHEN ULTIMOMOVIMIENTO.CLASE = 'S' THEN ULTIMOMOVIMIENTO.CLASE_BODEGA_ORIGEN
				ELSE ULTIMOMOVIMIENTO.CLASE_BODEGA_DESTINO
			END IN('30') THEN ICUENTA.CUENTAACTIVOS
			ELSE
			CASE
				WHEN
				CASE
					WHEN ULTIMOMOVIMIENTO.CLASE = 'S' THEN ULTIMOMOVIMIENTO.CLASE_BODEGA_ORIGEN
					ELSE ULTIMOMOVIMIENTO.CLASE_BODEGA_DESTINO
				END IN('40') THEN ICUENTA.CUENTAACTIVOI
				ELSE
				CASE
					WHEN
					CASE
						WHEN ULTIMOMOVIMIENTO.CLASE = 'S' THEN ULTIMOMOVIMIENTO.CLASE_BODEGA_ORIGEN
						ELSE ULTIMOMOVIMIENTO.CLASE_BODEGA_DESTINO
					END IN('50') THEN ICUENTA.CUENTAACTIVOR
					ELSE
					CASE
						WHEN
						CASE
							WHEN ULTIMOMOVIMIENTO.CLASE = 'S' THEN ULTIMOMOVIMIENTO.CLASE_BODEGA_ORIGEN
							ELSE ULTIMOMOVIMIENTO.CLASE_BODEGA_DESTINO
						END IN('80') THEN ICUENTA.CUENTAACTIVOC
						ELSE ''
					END
				END
			END
		END
	END AS "CUENTA CONTABLE",
 ULTIMOMOVIMIENTO.DESCRIPCION "OBSERVACIONES"
FROM DEVOLUTIVO
INNER JOIN INVENTARIO
     ON INVENTARIO.COMPANIA = DEVOLUTIVO.COMPANIA
    AND INVENTARIO.CODIGOELEMENTO  = DEVOLUTIVO.ELEMENTO
LEFT JOIN ESTADODEVOLUTIVO
    ON ESTADODEVOLUTIVO.ESTADO = DEVOLUTIVO.ESTADO
INNER JOIN DEPENDENCIA
    ON DEPENDENCIA.COMPANIA = DEVOLUTIVO.COMPANIA
    AND DEPENDENCIA.CODIGO   = DEVOLUTIVO.DEPENDENCIA
INNER JOIN TERCERO
     ON DEVOLUTIVO.COMPANIA = TERCERO.COMPANIA
    AND DEVOLUTIVO.RESPONSABLE   = TERCERO.NIT
LEFT JOIN DEPRECIAR ON
	DEVOLUTIVO.COMPANIA = DEPRECIAR.COMPANIA
	AND DEVOLUTIVO.ELEMENTO = DEPRECIAR.ELEMENTO
	AND DEVOLUTIVO.SERIE = DEPRECIAR.SERIE
INNER JOIN ULTIMOMOVIMIENTO ON
	ULTIMOMOVIMIENTO.COMPANIA = DEVOLUTIVO.COMPANIA
	AND ULTIMOMOVIMIENTO.ELEMENTO = DEVOLUTIVO.ELEMENTO
	AND ULTIMOMOVIMIENTO.SERIE = DEVOLUTIVO.SERIE
    AND ULTIMOMOVIMIENTO.TIPOMOVIMIENTO =
	CASE
		WHEN ULTIMOMOVIMIENTO.CLASE = 'S' THEN DEVOLUTIVO.TIPOMOVIMIENTOI
		ELSE DEVOLUTIVO.TIPOMOVIMIENTOF
	END
INNER JOIN INVENTARIO ICUENTA
     ON DEVOLUTIVO.COMPANIA   = ICUENTA.COMPANIA
    AND SUBSTR(DEVOLUTIVO.ELEMENTO,1, PCK_SYSMAN_UTL.FC_PAR (UN_COMPANIA          => DEVOLUTIVO.COMPANIA,
                                                             UN_NOMBRE            => 'DIGITOS AGRUPACION INVENTARIO',
                                                             UN_MODULO            => PCK_DATOS.FC_MODULOALMACEN,
                                                             UN_FECHA_PAR         => SYSDATE,
                                                             UN_IND_MAYUS         =>  -1)) = ICUENTA.CODIGOELEMENTO
WHERE DEVOLUTIVO.COMPANIA =   's$compania$s'
  AND DEVOLUTIVO.ELEMENTO BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s'
  AND (DEVOLUTIVO.PLACAANULADA IN (0)
  OR DEVOLUTIVO.PLACAANULADA NOT IN (0) AND DEVOLUTIVO.FECHAANULADA>TO_DATE('s$fechaCorte$s','DD/MM/YYYY'))
ORDER BY DEVOLUTIVO.ELEMENTO 