WITH C_DOCALMACENESPECIAL AS (
	s$consulta1831$s
)
SELECT 
  C_DOCALMACENESPECIAL.COMPANIA,
  C_DOCALMACENESPECIAL.CUENTAACTIVO,
  LPAD(C_DOCALMACENESPECIAL.ELEMENTO,PCK_SYSMAN_UTL.FC_PAR( UN_COMPANIA   => 's$compania$s'
,UN_NOMBRE    => 'DIGITOS AGRUPACION INVENTARIO'
,UN_MODULO    => PCK_DATOS.FC_MODULOALMACEN()
,UN_FECHA_PAR => SYSDATE
,UN_IND_MAYUS => 0)) AS GRUPO,
SUM(VENTRADAS - VSALIDAS) AS VALOR
FROM C_DOCALMACENESPECIAL 
GROUP BY C_DOCALMACENESPECIAL.COMPANIA,
  C_DOCALMACENESPECIAL.CUENTAACTIVO,
  LPAD(C_DOCALMACENESPECIAL.ELEMENTO,PCK_SYSMAN_UTL.FC_PAR( UN_COMPANIA   => 's$compania$s'
,UN_NOMBRE    => 'DIGITOS AGRUPACION INVENTARIO'
,UN_MODULO    => PCK_DATOS.FC_MODULOALMACEN()
,UN_FECHA_PAR => SYSDATE
,UN_IND_MAYUS => 0))
HAVING (SUM(VENTRADAS - VSALIDAS) NOT IN (0))
