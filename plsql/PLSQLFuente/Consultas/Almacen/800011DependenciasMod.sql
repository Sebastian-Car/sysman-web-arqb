SELECT * FROM (
    SELECT TIPOADJUDICACION.DESCRIPCION,
           ORDENDECOMPRA.VALORTOTAL,
           TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY') ANIO
    FROM ORDENDECOMPRA 
     	INNER JOIN TIPOADJUDICACION 
          	ON  ORDENDECOMPRA.COMPANIA  		= TIPOADJUDICACION.COMPANIA 
          	AND ORDENDECOMPRA.TIPOADJUDICACION  = TIPOADJUDICACION.CODIGO 
    WHERE ORDENDECOMPRA.COMPANIA         = 's$compania$s'
      AND ORDENDECOMPRA.TIPOADJUDICACION = 's$modalidad$s' 
      AND ORDENDECOMPRA.DEPENDENCIA      = s$dependencia$s
      AND TO_NUMBER(TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY')) 
           BETWEEN s$anioInicial$s  AND s$anioFinal$s
    GROUP BY  ORDENDECOMPRA.DEPENDENCIA,
              TIPOADJUDICACION.DESCRIPCION,
              ORDENDECOMPRA.VALORTOTAL,
              TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY')
)  PIVOT (SUM(VALORTOTAL) FOR ANIO IN (s$pivotConsulta$s))
