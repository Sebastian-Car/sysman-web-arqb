WITH C_RESUMENDEPENDENCIA AS (
SELECT 
  DEVOLUTIVO.COMPANIA,
  SUBSTR(ELEMENTO,0,3) CODELEMENTO,
  DEVOLUTIVO.DEPENDENCIA,
  SUM(DEVOLUTIVO.VALOR) SUMADEVALOR
FROM DEVOLUTIVO
GROUP BY DEVOLUTIVO.COMPANIA, SUBSTR(ELEMENTO,0,3), DEVOLUTIVO.DEPENDENCIA
HAVING DEVOLUTIVO.COMPANIA=s$compania$s
AND DEVOLUTIVO.DEPENDENCIA BETWEEN s$dependenciaInicial$s AND s$dependenciaFinal$s
ORDER BY DEVOLUTIVO.COMPANIA, SUBSTR(ELEMENTO,0,3)
)
SELECT DISTINCT 
  INVENTARIO.CODIGOELEMENTO,
  INVENTARIO.NOMBRELARGO,
  LENGTH(CODIGOELEMENTO) EXPR2,
  NVL(SUMADEVALOR,0) VALORT
FROM INVENTARIO
LEFT JOIN C_RESUMENDEPENDENCIA
	ON INVENTARIO.COMPANIA         = C_RESUMENDEPENDENCIA.COMPANIA
	AND INVENTARIO.CODIGOELEMENTO  = C_RESUMENDEPENDENCIA.CODELEMENTO
WHERE LENGTH(CODIGOELEMENTO)=TO_NUMBER(s$agrupacion$s)
	AND TO_NUMBER(NVL(SUMADEVALOR,0))<>0
	AND INVENTARIO.COMPANIA=s$compania$s
ORDER BY INVENTARIO.CODIGOELEMENTO
