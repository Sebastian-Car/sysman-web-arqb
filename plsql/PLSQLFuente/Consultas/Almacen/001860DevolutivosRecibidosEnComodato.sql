 SELECT INVENTARIO.COMPANIA,
    INVENTARIO.CODIGOELEMENTO,
    INVENTARIO.NOMBRELARGO,
    INVENTARIO.TIPO,
    DEVOLUTIVO.ESTADO,
    DEVOLUTIVO.VALOR VALORDEVOLUTIVO,
    SUBSTR(DEVOLUTIVO.ELEMENTO,0,s$digito$s) AGRUPACION,
    DEVOLUTIVO.SERIEDEVOLUTIVO,
    DEVOLUTIVO.SERIE PLACA,
    INVENTARIOAGRUPACION.NOMBRELARGO  DESCRIPCION
  FROM INVENTARIO
  LEFT JOIN DEVOLUTIVO
  ON INVENTARIO.COMPANIA        = DEVOLUTIVO.COMPANIA
  AND INVENTARIO.CODIGOELEMENTO = DEVOLUTIVO.ELEMENTO
  LEFT JOIN INVENTARIO INVENTARIOAGRUPACION
  ON INVENTARIOAGRUPACION.COMPANIA        = DEVOLUTIVO.COMPANIA
  AND INVENTARIOAGRUPACION.CODIGOELEMENTO = SUBSTR(DEVOLUTIVO.ELEMENTO,0,s$digito$s)
  INNER JOIN TIPOMOVIMIENTO
  ON DEVOLUTIVO.COMPANIA         = TIPOMOVIMIENTO.COMPANIA
  AND DEVOLUTIVO.TIPOMOVIMIENTOI = TIPOMOVIMIENTO.CODIGO
 WHERE INVENTARIO.COMPANIA = s$compania$s
 AND INVENTARIO.CODIGOELEMENTO BETWEEN  s$cmbElementoDesde$s AND s$cmbElementoHasta$s
 AND TIPOMOVIMIENTO.CLASE_BODEGA_ORIGEN IN ('90')
 AND TIPOMOVIMIENTO.CLASE_BODEGA_DESTINO IN ('20') 
