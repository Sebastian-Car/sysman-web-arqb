MERGE INTO CONSULTAS FIN USING (SELECT '002953ListadoPorPlacas' INFORME ,TO_CLOB(q'[SELECT  
	INVENTARIO.NOMBRELARGO, 
	DEVOLUTIVO.ELEMENTO AS CODIGOELEMENTODEV,  
	DEVOLUTIVO.SERIE, 
	DEVOLUTIVO.DESCRIPCION DEVOLUTIVODESCRIPCION,  
	DEVOLUTIVO.MARCA,  
	DEVOLUTIVO.SERIEDEVOLUTIVO,  
	DEPENDENCIA.NOMBRE, 
	TRIM(TERCERO.NOMBRE) EMPLEADO, 
	TRIM(NOMBRELARGO) || ' Marca: ' || DEVOLUTIVO.MARCA || ' - ' || DEVOLUTIVO.DESCRIPCION || (CASE TRIM(SERIEDEVOLUTIVO) WHEN '' THEN '' ELSE 'Serie:' || SERIEDEVOLUTIVO END) AS NOMBRELARGOS,  
	DEVOLUTIVO.VALOR AS VALORDEVOLUTIVO,  
	SUBSTR(CODIGOELEMENTO,0,3) AS AGRUPACION, 
	ESTADODEVOLUTIVO.DESCRIPCION ESTADODESCRIPCION, 
	DEVOLUTIVO.COMPANIA, 
	DEVOLUTIVO.PLACAANULADA
,
	DEVOLUTIVO.UBICACION 
FROM INVENTARIO 
	INNER JOIN DEVOLUTIVO 
		    ON INVENTARIO.COMPANIA 		 = DEVOLUTIVO.COMPANIA 
		   AND INVENTARIO.CODIGOELEMENTO = DEVOLUTIVO.ELEMENTO
	INNER JOIN DEPENDENCIA 
	        ON DEVOLUTIVO.COMPANIA       = DEPENDENCIA.COMPANIA 
	       AND DEVOLUTIVO.DEPENDENCIA    = DEPENDENCIA.CODIGO
	INNER JOIN RESPONSABLE 
			ON DEVOLUTIVO.COMPANIA       	   = RESPONSABLE.COMPANIA 
		   AND DEVOLUTIVO.RESPONSABLE     	   = RESPONSABLE.CEDULA
		   AND DEVOLUTIVO.SUCURSAL_RESPONSABLE = RESPONSABLE.SUCURSAL 	   
	INNER JOIN ESTADODEVOLUTIVO  
	  	    ON DEVOLUTIVO.ESTADO         = ESTADODEVOLUTIVO.ESTADO 
	INNER JOIN TERCERO 
		    ON TERCERO.COMPANIA 		 = RESPONSABLE.COMPANIA 
		   AND TERCERO.NIT				 = RESPONSABLE.CEDULA
		   AND TERCERO.SUCURSAL 		 = RESPONSABLE.SUCURSAL	  	    
WHERE DEVOLUTIVO.COMPANIA = s$compania$s    
  AND DEVOLUTIVO.SERIE BETWEEN s$serieDesde$s AND s$serieHasta$s  
  AND DEVOLUTIVO.PLACAANULADA IN (0) 
ORDER BY s$orden$s]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);