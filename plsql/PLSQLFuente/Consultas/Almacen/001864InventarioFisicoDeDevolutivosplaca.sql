SELECT DEVOLUTIVO.SERIEDEVOLUTIVO,
  DEVOLUTIVO.DEPENDENCIA,
  DEVOLUTIVO.SERIE,
  DEVOLUTIVO.ESTADO,
  DEVOLUTIVO.VALOR             TVALOR,
  DEVOLUTIVO.SERIEANTERIOR      PLACAANTERIOR,
  DEVOLUTIVO.FECHAADQUISICION,
  SUBSTR (DEVOLUTIVO.ELEMENTO,3 ,s$digito$s)  AGRUPACION,
  DEVOLUTIVO.DESCRIPCION     DESCRIPCIOND,
  INVENTARIO.NOMBRELARGO,
  ESTADODEVOLUTIVO.DESCRIPCION     DESCRIPCIONE,
  DEPENDENCIA.NOMBRE,
  DEVOLUTIVO.ELEMENTO,
  INVENTARIO_1.NOMBRELARGO  GRUPO
FROM DEVOLUTIVO
INNER JOIN INVENTARIO
ON DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA
AND DEVOLUTIVO.ELEMENTO  = INVENTARIO.CODIGOELEMENTO
INNER JOIN DEPENDENCIA
ON DEVOLUTIVO.COMPANIA   = DEPENDENCIA.COMPANIA
AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO
INNER JOIN ESTADODEVOLUTIVO
ON DEVOLUTIVO.ESTADO = ESTADODEVOLUTIVO.ESTADO
INNER JOIN INVENTARIO  INVENTARIO_1
ON DEVOLUTIVO.COMPANIA = INVENTARIO_1.COMPANIA
WHERE DEVOLUTIVO.COMPANIA         = s$compania$s
AND DEVOLUTIVO.FECHAADQUISICION   <= sysdate
AND SUBSTR (DEVOLUTIVO.ELEMENTO,3 ,s$digito$s) = INVENTARIO_1.CODIGOELEMENTO
AND DEVOLUTIVO.PLACAANULADA IN (0)
