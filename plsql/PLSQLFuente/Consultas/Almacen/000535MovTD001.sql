SELECT MOVIMIENTO,
    FECHA_MOV,
    DESCRIPCION_MOV,
    CENTRODECOSTO,
    CENTRODECOSTO_NOM,
    ELEMENTO,
    DESCRIPCION_INV,
    UNIDAD,
    SERIE,
    VALORTOTAL_D,
    DEPENDENCIA_ORIGEN,
    DEPENDENCIA_ORIGEN_NOM,
    DEPENDENCIA_DESTINO_NOM,
    RESPONSABLE_ORIGEN_NOM,
    RESPONSABLE_ORIGEN_CARGO,
    RESPONSABLE_DESTINO_NOM,
    RESPONSABLE_DESTINO_CARGO,
    (TIPOMOVIMIENTO || MOVIMIENTO) GRUPO
FROM V_MOVIMIENTO_INFORME
WHERE COMPANIA = s$compania$s 
    AND TIPOMOVIMIENTO = 's$tipoMovimiento$s'
    AND MOVIMIENTO BETWEEN s$movimientoInicial$s AND s$movimientoFinal$s
