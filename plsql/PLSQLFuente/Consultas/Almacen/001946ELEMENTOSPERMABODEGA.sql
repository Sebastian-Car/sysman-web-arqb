SELECT D_MOVIMIENTO.TIPOMOVIMIENTO,
    D_MOVIMIENTO.MOVIMIENTO,
    D_MOVIMIENTO.ELEMENTO,
    D_MOVIMIENTO.SERIE,
    DEVOLUTIVO.ESTADO,
    DEVOLUTIVO.SERIEDEVOLUTIVO,
    D_MOVIMIENTO.VALORUNITARIO VALORT,
    INVENTARIO.NOMBRECORTO,
    UPPER(PCK_SYSMAN_UTL.FC_NOMBRE_MES(PCK_SYSMAN_UTL.FC_MES(D_MOVIMIENTO.FECHA))) MESES,
    CASE WHEN (TIPOMOVIMIENTO.CLASE IN('E')  AND TIPOMOVIMIENTO.CONCEPTO IN('R'))
    THEN '00000000000'
    ELSE 
      CASE WHEN TIPOMOVIMIENTO.TIPOPESONA IN('P') THEN '00000000000' ELSE  MOVIMIENTO.TERCERO END
    END  RESPONSABLE,
    CASE WHEN (TIPOMOVIMIENTO.CLASE  IN('E') AND TIPOMOVIMIENTO.CONCEPTO IN('R'))
    THEN '00000000000' 
    ELSE DEVOLUTIVO.DEPENDENCIA 
    END  DEPENDENCIA,
    DEVOLUTIVO.FECHAANULADA
  FROM MOVIMIENTO
  INNER JOIN D_MOVIMIENTO
  ON MOVIMIENTO.COMPANIA        = D_MOVIMIENTO.COMPANIA
  AND MOVIMIENTO.TIPOMOVIMIENTO = D_MOVIMIENTO.TIPOMOVIMIENTO
  AND MOVIMIENTO.NUMERO         = D_MOVIMIENTO.MOVIMIENTO
  LEFT JOIN INVENTARIO
  ON D_MOVIMIENTO.COMPANIA  = INVENTARIO.COMPANIA
  AND D_MOVIMIENTO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
  LEFT JOIN DEVOLUTIVO
  ON D_MOVIMIENTO.COMPANIA  = DEVOLUTIVO.COMPANIA
  AND D_MOVIMIENTO.ELEMENTO = DEVOLUTIVO.ELEMENTO
  AND D_MOVIMIENTO.SERIE    = DEVOLUTIVO.SERIE 
  INNER JOIN TIPOMOVIMIENTO
  ON MOVIMIENTO.COMPANIA = TIPOMOVIMIENTO.COMPANIA
  AND MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
  WHERE MOVIMIENTO.COMPANIA = s$compania$s
  AND D_MOVIMIENTO.ELEMENTO BETWEEN s$elementoInicial$s AND s$elementoFinal$s
  GROUP BY D_MOVIMIENTO.TIPOMOVIMIENTO,
    D_MOVIMIENTO.MOVIMIENTO,
    D_MOVIMIENTO.ELEMENTO,
    D_MOVIMIENTO.SERIE,
    DEVOLUTIVO.ESTADO,
    DEVOLUTIVO.SERIEDEVOLUTIVO,
    D_MOVIMIENTO.VALORUNITARIO,
    INVENTARIO.NOMBRECORTO,
    UPPER(PCK_SYSMAN_UTL.FC_NOMBRE_MES(PCK_SYSMAN_UTL.FC_MES(D_MOVIMIENTO.FECHA))),
    CASE WHEN (TIPOMOVIMIENTO.CLASE IN('E')  AND TIPOMOVIMIENTO.CONCEPTO IN('R'))
    THEN '00000000000'
    ELSE 
    CASE WHEN TIPOMOVIMIENTO.TIPOPESONA IN('P') 
    THEN '00000000000' ELSE  MOVIMIENTO.TERCERO 
    END END,
    CASE WHEN (TIPOMOVIMIENTO.CLASE  IN('E') AND TIPOMOVIMIENTO.CONCEPTO IN('R'))
    THEN '00000000000' 
    ELSE DEVOLUTIVO.DEPENDENCIA END,
    DEVOLUTIVO.FECHAANULADA
  ORDER BY  D_MOVIMIENTO.ELEMENTO
