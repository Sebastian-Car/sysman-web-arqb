SELECT DISTINCT
    DEVOLUTIVO.ELEMENTO,
    DEVOLUTIVO.SERIE, 
    DEVOLUTIVO.DESCRIPCION,
    DEVOLUTIVO.MARCA, 
    INVENTARIO.NOMBRELARGO, 
    DEPRECIAR.VLRAJUSTADO,
    DEPRECIAR.VLRACUMULADOAJ,
    DEPRECIAR.VLRDEPRECIACIONAJ,
    DEPRECIAR.COSTOAJUSTADO,
    DEPRECIAR.VLRELEMENTO,
    DEPRECIAR.AJDEPACUMULADA,
    PCK_ALMACEN.FC_NOMBREINVENTARIO(DEPRECIAR.COMPANIA,SUBSTR(DEVOLUTIVO.ELEMENTO,0,s$agrupacion$s),0) NOMBREINVENTARIO,
    NOMBRELARGO|| DEVOLUTIVO.DESCRIPCION || CASE WHEN TRIM(DEVOLUTIVO.MARCA) IS NULL THEN '' ELSE ' MARCA: '||DEVOLUTIVO.MARCA END NOMBRELARGOS,
    SUBSTR(DEVOLUTIVO.ELEMENTO, 0, s$agrupacion$s) GRUPO,
    DEVOLUTIVO.PLACAANULADA,
    TO_CHAR (DEVOLUTIVO.FECHAANULADA,'DD/MM/YYYY')FECHAANULADA,
    SUM (DEPRECIAR.VLRELEMENTO) + (DEPRECIAR.AJUSTE_ACUM_ELEM_COMJ) AS COSTOAJUS,
    SUM (DEPRECIAR.DEPACUMULADAAJ) + (DEPRECIAR.VLR_DEPRECIACION_ACUM_COMJ)+ (DEPRECIAR.AJUSTE_ACUM_DEPRECIAR_COMJ) AS VLRDPRACUMAJUSCCJ,
    (((Depreciar.VLRLIBROS + AJUSTE_ACUM_ELEM_COMJ)- DEPRECIAR.AJUSTE_ACUM_DEPRECIAR_COMJ)- DEPRECIAR.VLR_DEPRECIACION_ACUM_COMJ) AS VLRLIBROSCCCJ, 
    NVL(Devolutivo.FECHAANULADA, TO_DATE('31/12/3000', 'DD/MM/YYYY')) AS EXPR1
FROM DEVOLUTIVO 
    LEFT JOIN INVENTARIO 
ON DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA 
    AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
LEFT JOIN DEPRECIAR 
    ON DEVOLUTIVO.COMPANIA = DEPRECIAR.COMPANIA 
    AND DEVOLUTIVO.ELEMENTO = DEPRECIAR.ELEMENTO
    AND DEVOLUTIVO.SERIE = DEPRECIAR.SERIE
WHERE DEVOLUTIVO.COMPANIA = 's$compania$s'
    AND DEVOLUTIVO.ELEMENTO BETWEEN s$elementoInicial$s AND s$elementoFinal$s 
    AND DEVOLUTIVO.SERIE BETWEEN s$placaInicial$s AND s$placaFinal$s 
    AND TO_CHAR (DEPRECIAR.PERIODO,'YYYY')=s$ano$s 
    AND TO_CHAR (DEPRECIAR.PERIODO,'MM') = s$mes$s
    AND TO_DATE(TO_CHAR(NVL(FECHAANULADA,TO_DATE('31/12/3000')), 'DD/MM/YYYY'), 'DD/MM/YYYY')>TO_DATE('s$ultimoDia$s', 'DD/MM/YYYY')
GROUP BY 
    DEVOLUTIVO.ELEMENTO,
    DEVOLUTIVO.SERIE, 
    DEVOLUTIVO.DESCRIPCION,
    DEVOLUTIVO.MARCA,
    PCK_ALMACEN.FC_NOMBREINVENTARIO(DEPRECIAR.COMPANIA,SUBSTR(DEVOLUTIVO.ELEMENTO,0,s$agrupacion$s),0),
    INVENTARIO.NOMBRELARGO,  
    SUBSTR(DEVOLUTIVO.ELEMENTO,0,s$agrupacion$s),
    DEVOLUTIVO.PLACAANULADA,
    DEVOLUTIVO.FECHAANULADA,
    DEPRECIAR.VLRAJUSTADO,
    DEPRECIAR.VLRACUMULADOAJ,
    DEPRECIAR.VLRDEPRECIACIONAJ,
    DEPRECIAR.COSTOAJUSTADO,
    DEPRECIAR.VLRELEMENTO,
    DEPRECIAR.AJDEPACUMULADA,
    DEPRECIAR.AJUSTE_ACUM_ELEM_COMJ,
    DEPRECIAR.VLR_DEPRECIACION_ACUM_COMJ,
    DEPRECIAR.AJUSTE_ACUM_DEPRECIAR_COMJ,
    DEPRECIAR.VLRLIBROS
ORDER BY 
    DEVOLUTIVO.ELEMENTO, 
    DEVOLUTIVO.SERIE