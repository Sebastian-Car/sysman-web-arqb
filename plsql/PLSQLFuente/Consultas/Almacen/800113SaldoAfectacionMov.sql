SELECT 
   TIPOMOVIMIENTO
  ,TO_CHAR(MOVIMIENTO)         MOVIMIENTO
  ,CANTIDAD                    CANTIDAD
  ,CANTIDADAFECTADA            CANTIDADAFECTADA 
  ,CANTIDAD - CANTIDADAFECTADA SALDO 
FROM D_MOVIMIENTO 
WHERE COMPANIA       = s$compania$s
  AND TIPOMOVIMIENTO = s$tipoMovimiento$s
  AND MOVIMIENTO     = s$movimiento$s
