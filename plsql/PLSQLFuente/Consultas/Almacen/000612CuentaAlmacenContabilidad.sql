MERGE INTO CONSULTAS FIN USING ( SELECT '000612CuentaAlmacenContabilidad' AS INFORME_VAL, TO_CLOB(q'[WITH VISTA_CUENTAALMACEN AS (
  SELECT V_BASE_CUENTAALMACEN.BODEGA_DESTINO1,
       V_BASE_CUENTAALMACEN.CLASEFINAL,
       V_BASE_CUENTAALMACEN.CUENTA,
       PLAN_CONTABLE.NOMBRE NOMBRECUENTA,
       V_BASE_CUENTAALMACEN.NOMBREBODEGA DEPEND,
       V_BASE_CUENTAALMACEN.TIPO TIPO_COD,
       DECODE(V_BASE_CUENTAALMACEN.TIPO,'C' ,'Consumo'
                                       ,'D' ,'Devolutivos'
                                       ,'E' ,'Recibidos en Comodato'
                                       ,'EC','Entregados en Comodato'
                                       ,'M' ,'Consumo Controlado'
                                       ,'N' ,'ActivosFijos'
                                       ,'S' ,'Servicios') TIPO,
       SUM(CASE WHEN (V_BASE_CUENTAALMACEN.FECHA < s$fechaInicial$s)
            THEN NVL(V_BASE_CUENTAALMACEN.VALOR_ENTRADAS,0)- NVL(V_BASE_CUENTAALMACEN.VALOR_SALIDAS,0)
                    +NVL(ABS(V_BASE_CUENTAALMACEN.AJUSTESDEBITO),0)  - NVL(ABS(V_BASE_CUENTAALMACEN.AJUSTESCREDITO),0) ELSE 0 END ) AS SUMADESALDOANTERIOR,
       SUM(CASE WHEN (V_BASE_CUENTAALMACEN.FECHA >= s$fechaInicial$s AND V_BASE_CUENTAALMACEN.FECHA <= s$fechaFinal$s)
                 THEN NVL(V_BASE_CUENTAALMACEN.VALOR_ENTRADAS,0) ELSE 0 END ) AS SUMADEENTRADAS,
       SUM(CASE WHEN (V_BASE_CUENTAALMACEN.FECHA >= s$fechaInicial$s AND V_BASE_CUENTAALMACEN.FECHA <= s$fechaFinal$s)
                THEN NVL(V_BASE_CUENTAALMACEN.VALOR_SALIDAS,0) ELSE 0 END )  AS SUMADESALIDAS,
       SUM(CASE WHEN (V_BASE_CUENTAALMACEN.FECHA >= s$fechaInicial$s AND V_BASE_CUENTAALMACEN.FECHA <= s$fechaFinal$s)
                  THEN NVL(ABS(V_BASE_CUENTAALMACEN.AJUSTESDEBITO),0) ELSE 0 END )  AS SUMADEAJUSTESDEBITO,
       SUM(CASE WHEN (V_BASE_CUENTAALMACEN.FECHA >= s$fechaInicial$s AND V_BASE_CUENTAALMACEN.FECHA <= s$fechaFinal$s)
                THEN NVL(ABS(V_BASE_CUENTAALMACEN.AJUSTESCREDITO),0) ELSE 0 END )  AS SUMADEAJUSTESCREDITO,
       SUM(NVL(V_BASE_CUENTAALMACEN.VALOR_ENTRADAS,0)-NVL(V_BASE_CUENTAALMACEN.VALOR_SALIDAS,0)+
           NVL(ABS(V_BASE_CUENTAALMACEN.AJUSTESDEBITO),0)  - NVL(ABS(V_BASE_CUENTAALMACEN.AJUSTESCREDITO),0))  AS SUMADESALDOFINAL
FROM V_BASE_CUENTAALMACEN INNER JOIN BODEGA
  ON V_BASE_CUENTAALMACEN.COMPANIA        = BODEGA.COMPANIA
 AND V_BASE_CUENTAALMACEN.BODEGA_DESTINO1 = BODEGA.CODIGO
 LEFT JOIN (SELECT COMPANIA, ANO, CODIGO, NOMBRE
             FROM PLAN_CONTABLE
             WHERE COMPANIA = s$compania$s
               AND ANO      = TO_NUMBER(TO_CHAR(s$fechaFinal$s, 'YYYY'))) PLAN_CONTABLE
   ON PLAN_CONTABLE.COMPANIA  = V_BASE_CUENTAALMACEN.COMPANIA
  AND PLAN_CONTABLE.CODIGO    = V_BASE_CUENTAALMACEN.CUENTA
  AND PLAN_CONTABLE.ANO       = TO_NUMBER(TO_CHAR(s$fechaFinal$s, 'YYYY'))
 WHERE V_BASE_CUENTAALMACEN.COMPANIA= s$compania$s
  AND V_BASE_CUENTAALMACEN.FECHA BETWEEN s$fechaCorte$s AND s$fechaFinal$s
  AND V_BASE_CUENTAALMACEN.IND_REG NOT IN (0)
  AND BODEGA.MOSTRAR NOT IN(0)
  AND CASE
      WHEN s$sinConsumoControlado$s NOT IN (0]') || TO_CLOB(q'[)
      THEN (TIPO||BODEGA_DESTINO1)
      ELSE ' '
  END NOT IN ('M30')
GROUP BY  V_BASE_CUENTAALMACEN.BODEGA_DESTINO1,
          V_BASE_CUENTAALMACEN.CLASEFINAL,
          V_BASE_CUENTAALMACEN.NOMBREBODEGA,
          V_BASE_CUENTAALMACEN.TIPO,
          V_BASE_CUENTAALMACEN.CUENTA,
          PLAN_CONTABLE.NOMBRE)
SELECT  BODEGA_DESTINO1 ,
        CLASEFINAL ,
        CUENTA,
        NOMBRECUENTA,
        DEPEND,
        TIPO_COD,
        TIPO,
        SUMADESALDOANTERIOR,
        SUMADEENTRADAS,
        SUMADESALIDAS,
        SUMADEAJUSTESDEBITO,
        SUMADEAJUSTESCREDITO,
        SUMADESALDOANTERIOR + SUMADEENTRADAS - SUMADESALIDAS + SUMADEAJUSTESDEBITO - SUMADEAJUSTESCREDITO SUMADESALDOFINAL
FROM   VISTA_CUENTAALMACEN
WHERE  ABS(SUMADESALDOANTERIOR) + ABS(SUMADEENTRADAS) +ABS( SUMADESALIDAS) + ABS(SUMADEAJUSTESDEBITO) + ABS(SUMADEAJUSTESCREDITO) <>0
ORDER BY CLASEFINAL, BODEGA_DESTINO1, TIPO_COD, CUENTA]') AS CONSULTA_VAL, 10 AS APLICACION_VAL, TO_CLOB(q'[]') AS CONSULTA_OPCIONAL_VAL, (SELECT MIN(ROWID) FROM CONSULTAS WHERE INFORME = '000612CuentaAlmacenContabilidad') AS RID_DESTINO  FROM DUAL ) INI ON (FIN.ROWID = INI.RID_DESTINO)  WHEN MATCHED THEN   UPDATE SET FIN.CONSULTA = INI.CONSULTA_VAL, FIN.APLICACION = INI.APLICACION_VAL, FIN.CONSULTA_OPCIONAL = INI.CONSULTA_OPCIONAL_VAL, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN   INSERT (INFORME, CONSULTA, APLICACION, CONSULTA_OPCIONAL, DATE_CREATED)   VALUES (INI.INFORME_VAL, INI.CONSULTA_VAL, INI.APLICACION_VAL, INI.CONSULTA_OPCIONAL_VAL, SYSDATE);