SELECT  PREDIOS.ID_PREDIO, 
              USOS.DESCRIPCION
 FROM    PREDIOS 
 LEFT JOIN (USOS_PREDIO 
        LEFT JOIN USOS ON (USOS_PREDIO.CODIGO_USO = USOS.CODIGO_USO 
                       AND USOS_PREDIO.COMPANIA = USOS.COMPANIA))
          ON (PREDIOS.COMPANIA = USOS_PREDIO.COMPANIA 
          AND PREDIOS.ID_PREDIO = USOS_PREDIO.ID_PREDIO)
 WHERE PREDIOS.COMPANIA =  s$compania$s
 AND   PREDIOS.ID_PREDIO = $P{PR_ID_PREDIO}
