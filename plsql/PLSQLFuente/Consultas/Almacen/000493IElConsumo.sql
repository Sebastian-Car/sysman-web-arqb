SELECT D_MOVIMIENTO.COMPANIA,  
    D_MOVIMIENTO.ELEMENTO,  
    INVENTARIO.NOMBRELARGO,  
    MOVIMIENTO.DEPENDENCIA_DESTINO DEPENDENCIA,  
    DEPENDENCIA.NOMBRE NOMBREDEP,  
    D_MOVIMIENTO.CANTIDAD,  
    D_MOVIMIENTO.VALORTOTAL,  
    MOVIMIENTO.TERCERO CEDULA,  
    TERCERO.NOMBRE NOMBRERES,  
    INVENTARIO.TIPO,  
    INVENTARIO.UNIDAD,  
    TO_CHAR(D_MOVIMIENTO.FECHA,'DD/MM/YYYY') FECHA,  
    D_MOVIMIENTO.TIPOMOVIMIENTO,  
    D_MOVIMIENTO.MOVIMIENTO 
FROM (((D_MOVIMIENTO INNER JOIN (MOVIMIENTO INNER JOIN DEPENDENCIA ON  
                        MOVIMIENTO.COMPANIA = DEPENDENCIA.COMPANIA  
                        AND MOVIMIENTO.DEPENDENCIA_DESTINO = DEPENDENCIA.CODIGO) ON  
                    D_MOVIMIENTO.COMPANIA = MOVIMIENTO.COMPANIA  
                    AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO  
                    AND D_MOVIMIENTO.MOVIMIENTO = MOVIMIENTO.NUMERO) INNER JOIN INVENTARIO ON  
                D_MOVIMIENTO.COMPANIA = INVENTARIO.COMPANIA  
                AND D_MOVIMIENTO.ELEMENTO = INVENTARIO.CODIGOELEMENTO) INNER JOIN TIPOMOVIMIENTO ON  
            D_MOVIMIENTO.COMPANIA = TIPOMOVIMIENTO.COMPANIA  
            AND D_MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO) LEFT JOIN TERCERO ON 
        MOVIMIENTO.COMPANIA = TERCERO.COMPANIA  
        AND MOVIMIENTO.TERCERO = TERCERO.NIT 
        AND MOVIMIENTO.SUCURSAL = TERCERO.SUCURSAL 
WHERE D_MOVIMIENTO.COMPANIA = s$compania$s  
    AND D_MOVIMIENTO.ELEMENTO >= 's$elementoInicial$s'  
    AND D_MOVIMIENTO.ELEMENTO <= 's$elementoFinal$s' 
    AND MOVIMIENTO.DEPENDENCIA_DESTINO >= 's$depInicial$s' 
    AND MOVIMIENTO.DEPENDENCIA_DESTINO <= 's$depFinal$s' 
    AND MOVIMIENTO.CLASE_BODEGA_DESTINO NOT IN (20) 
    AND INVENTARIO.TIPO = 's$clase$s' 
    AND D_MOVIMIENTO.FECHA BETWEEN s$fechaDesde$s AND s$fechaHasta$s  
    AND TIPOMOVIMIENTO.CLASE='S'  
    AND D_MOVIMIENTO.IND_REG=-1 
ORDER BY D_MOVIMIENTO.COMPANIA