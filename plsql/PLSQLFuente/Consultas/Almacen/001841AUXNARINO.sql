MERGE INTO CONSULTAS FIN USING (SELECT '001841AUXNARINO' INFORME ,TO_CLOB(q'[SELECT 
  MOVIMIENTO.TIPOMOVIMIENTO,
  MOVIMIENTO.NUMERO,
  D_MOVIMIENTO.ELEMENTO,
  D_MOVIMIENTO.SERIE,
  SUM(D_MOVIMIENTO.CANTIDAD) AS SUMADECANTIDAD,
  MOVIMIENTO.FECHA,
  SUM(D_MOVIMIENTO.VALORTOTAL) AS SUMADEVALORTOTAL,
  INVENTARIO.NOMBRELARGO,
  MOVIMIENTO.RESPONSABLE_DESTINO CEDULA,
  TERCERO.NOMBRE
FROM D_MOVIMIENTO
INNER JOIN MOVIMIENTO
  ON D_MOVIMIENTO.COMPANIA               = MOVIMIENTO.COMPANIA
  AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO
  AND D_MOVIMIENTO.MOVIMIENTO         = MOVIMIENTO.NUMERO
INNER JOIN INVENTARIO
  ON D_MOVIMIENTO.COMPANIA  = INVENTARIO.COMPANIA
  AND D_MOVIMIENTO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
LEFT JOIN TERCERO
  ON MOVIMIENTO.COMPANIA      = TERCERO.COMPANIA
  AND MOVIMIENTO.RESPONSABLE_DESTINO     = TERCERO.NIT
  AND MOVIMIENTO.SUCURSAL_RESDESTINO    = TERCERO.SUCURSAL
WHERE MOVIMIENTO.COMPANIA = s$compania$s
     AND D_MOVIMIENTO.ELEMENTO BETWEEN 's$elementoInicial$s'  AND 's$elementoFinal$s' 
     AND MOVIMIENTO.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
     AND MOVIMIENTO.RESPONSABLE_DESTINO BETWEEN 's$responsableInicial$s' AND 's$responsableFinal$s' 
GROUP BY 
  MOVIMIENTO.TIPOMOVIMIENTO,
  MOVIMIENTO.NUMERO,
  D_MOVIMIENTO.ELEMENTO,
  D_MOVIMIENTO.SERIE,
  MOVIMIENTO.FECHA,
  INVENTARIO.NOMBRELARGO,
    MOVIMIENTO.RESPONSABLE_DESTINO,
  TERCERO.NOMBRE
ORDER BY MOVIMIENTO.FECHA]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);