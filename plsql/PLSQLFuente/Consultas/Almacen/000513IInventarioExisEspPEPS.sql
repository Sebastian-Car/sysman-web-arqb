MERGE INTO CONSULTAS FIN USING (SELECT '000513IInventarioExisEspPEPS' INFORME ,TO_CLOB(q'[SELECT 
INVENTARIO.CODIGOELEMENTO, 
INVENTARIO.NOMBRELARGO, 
INVENTARIO.UNIDAD, 
D_MOVIMIENTO.VALORUNITARIO, 
D_MOVIMIENTO.SALDO_PEPS, 
(D_MOVIMIENTO.VALORUNITARIO*D_MOVIMIENTO.SALDO_PEPS) AS VLRTOTAL,
SIG.NOMBRELARGO AS PADRE,
SIG.CODIGOELEMENTO AS CODPADRE
FROM V_MOVIMIENTO INNER JOIN D_MOVIMIENTO
  ON V_MOVIMIENTO.COMPANIA = D_MOVIMIENTO.COMPANIA
  AND V_MOVIMIENTO.TIPOMOVIMIENTO = D_MOVIMIENTO.TIPOMOVIMIENTO
  AND V_MOVIMIENTO.NUMERO = D_MOVIMIENTO.MOVIMIENTO
INNER JOIN INVENTARIO 
  ON D_MOVIMIENTO.COMPANIA = INVENTARIO.COMPANIA
  AND D_MOVIMIENTO.ELEMENTO = INVENTARIO.CODIGOELEMENTO 
INNER JOIN INVENTARIO SIG 
  ON D_MOVIMIENTO.COMPANIA = SIG.COMPANIA
  AND SUBSTR(D_MOVIMIENTO.ELEMENTO,1,'s$digitosinventario$s') = SIG.CODIGOELEMENTO 
WHERE INVENTARIO.COMPANIA=s$compania$s
AND INVENTARIO.CODIGOELEMENTO>='s$elementoDesde$s'
And INVENTARIO.CODIGOELEMENTO<='s$elementoHasta$s'
AND INVENTARIO.TIENEMOVIMIENTO<>0
AND V_MOVIMIENTO.CLASE='E'
AND D_MOVIMIENTO.SALDO_PEPS<>0
AND INVENTARIO.TIPO = 'C'
UNION ALL
SELECT
INVENTARIO.CODIGOELEMENTO,
INVENTARIO.NOMBRELARGO,
INVENTARIO.UNIDAD,
INVENTARIO.VLRUNITARIOPROM VALORUNITARIO,
INVENTARIO.EXISTENCIA SALDO_PEPS,
(INVENTARIO.EXISTENCIA*INVENTARIO.VLRUNITARIOPROM) AS VLRTOTAL,
SIG.NOMBRELARGO AS PADRE,
SIG.CODIGOELEMENTO AS CODPADRE
FROM INVENTARIO
INNER JOIN INVENTARIO SIG 
  ON INVENTARIO.COMPANIA = SIG.COMPANIA
  AND SUBSTR(INVENTARIO.CODIGOELEMENTO,1,'s$digitosinventario$s') = SIG.CODIGOELEMENTO 
WHERE INVENTARIO.COMPANIA=s$compania$s
AND INVENTARIO.CODIGOELEMENTO Between 's$elementoDesde$s' And 's$elementoHasta$s'
AND INVENTARIO.EXISTENCIA<>0
AND INVENTARIO.TIENEMOVIMIENTO<>0
AND INVENTARIO.TIPO NOT IN ('C')]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);