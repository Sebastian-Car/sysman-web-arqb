WITH UltimoMovimientoC AS (SELECT
	D_Movimiento.COMPANIA,
	D_Movimiento.ELEMENTO,
	MAX(D_Movimiento.SALDOKARDEX)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS CSaldo,
	MAX(D_Movimiento.VALORSALDO)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS VSaldo,
	MAX(D_Movimiento.VLRUNITARIOPROM)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS VPromedio
FROM D_Movimiento
	 INNER JOIN V_MOVIMIENTO MOVIMIENTO ON 
	 D_Movimiento.COMPANIA = MOVIMIENTO.COMPANIA AND 
	 D_Movimiento.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO AND 
	 D_Movimiento.MOVIMIENTO = MOVIMIENTO.NUMERO
WHERE D_MOVIMIENTO.COMPANIA=s$compania$s 
  AND D_MOVIMIENTO.ELEMENTO 
BETWEEN s$ELEMENTODESDE$s AND s$ELEMENTOHASTA$s 
  AND D_MOVIMIENTO.FECHA<=s$FECHA$s 
  AND D_MOVIMIENTO.IND_REG NOT IN (0)
GROUP BY 
	D_Movimiento.COMPANIA,
	D_Movimiento.ELEMENTO
	)
	
SELECT 
	Inventario.COMPANIA, 
	Inventario.CODIGOELEMENTO,
	Inventario.NOMBRELARGO, 
	s$ORDEN2$s ORDEN2,
	s$ORDEN3$s ORDEN3,
	Inventario.TIPO,
	Inventario.REFERENCIA, 
	PCK_ALMACEN.FC_NOMBREINVENTARIO(INVENTARIO.COMPANIA,SUBSTR(CODIGOELEMENTO,0,s$PR_DIGITOSAGRUPACIONINV$s),0) AS NOMBREINVENTARIO,
	SUBSTR(CODIGOELEMENTO,0,s$PR_DIGITOSAGRUPACIONINV$s) AS SIG,
	UltimoMovimientoC.VPromedio,
	NVL(UltimoMovimientoC.CSALDO,0) TCANTIDAD2,
	UNIDAD.NOMBRE, 
	LENGTH(CODIGOELEMENTO) AS EXPR1,
	UltimoMovimientoC.VSaldo
FROM Inventario
    LEFT JOIN UltimoMovimientoC ON 
	Inventario.COMPANIA = UltimoMovimientoC.Compania AND
	Inventario.CODIGOELEMENTO = UltimoMovimientoC.Elemento
	LEFT JOIN UNIDAD ON 
	Inventario.UNIDAD = UNIDAD.UNIDAD
WHERE Inventario.COMPANIA=s$compania$s 
  AND Inventario.CODIGOELEMENTO
BETWEEN s$ELEMENTODESDE$s AND s$ELEMENTOHASTA$s 
  AND LENGTH(CODIGOELEMENTO) >= s$PR_DIGITOSAGRUPACIONINV$s
  AND Inventario.TIENEMOVIMIENTO NOT IN (0)  
    s$SALDOINVSUMAXISTROSDEVAFECHA$s  
ORDER BY Inventario.CODIGOELEMENTO
