SELECT 
  SUBSTR(DEVOLUTIVO.ELEMENTO,0,5) AGRUPACION,
  DEVOLUTIVO.RESPONSABLE CEDULA,
  DEVOLUTIVO.DEPENDENCIA,
  DEPENDENCIA.NOMBRE DEPENDENCIA_NOM,
  ESTADODEVOLUTIVO.DESCRIPCION,
  DEVOLUTIVO.ESTADO,
  CASE WHEN DEVOLUTIVO.MARCA IS NULL OR DEVOLUTIVO.MARCA = '.'
       THEN INVENTARIO.NOMBRELARGO ||' '||DEVOLUTIVO.DESCRIPCION
       ELSE  INVENTARIO.NOMBRELARGO ||' '||'Marca: ' ||DEVOLUTIVO.MARCA||' '||DEVOLUTIVO.DESCRIPCION END NOMBRELARGO, 
  DEVOLUTIVO.RESPONSABLE,
  TERCERO.NOMBRE RESPONSABLE_NOM,
  DEVOLUTIVO.SERIE,
  DEVOLUTIVO.SERIEDEVOLUTIVO, 
  DEVOLUTIVO.VALOR TVALOR, 
  PCK_ALMACEN.FC_NOMBREINVENTARIO(DEVOLUTIVO.COMPANIA,SUBSTR(DEVOLUTIVO.ELEMENTO,1),0) NOMBREINVENTARIO 
  FROM DEVOLUTIVO
  INNER JOIN INVENTARIO
  ON DEVOLUTIVO.COMPANIA  = INVENTARIO.COMPANIA
  AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
  INNER JOIN DEPENDENCIA
  ON DEVOLUTIVO.COMPANIA     = DEPENDENCIA.COMPANIA
  AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO
  INNER JOIN TERCERO
  ON DEVOLUTIVO.COMPANIA              = TERCERO.COMPANIA
  AND DEVOLUTIVO.RESPONSABLE          = TERCERO.NIT
  AND DEVOLUTIVO.SUCURSAL_RESPONSABLE = TERCERO.SUCURSAL
  INNER JOIN ESTADODEVOLUTIVO
  ON DEVOLUTIVO.ESTADO      = ESTADODEVOLUTIVO.ESTADO
WHERE DEVOLUTIVO.COMPANIA    = s$compania$s
  AND DEVOLUTIVO.RESPONSABLE  = 's$responsable$s'
  AND DEVOLUTIVO.PLACAANULADA IN (0)