SELECT DEVOLUTIVO.COMPANIA,
  INVENTARIO.NOMBRELARGO,
  DEVOLUTIVO.ELEMENTO AS CODIGOELEMENTODEV,
  DEVOLUTIVO.SERIE,
  DEVOLUTIVO.DESCRIPCION,
  DEVOLUTIVO.MARCA,
  DEVOLUTIVO.SERIEDEVOLUTIVO,
  DEPENDENCIA.NOMBRE DEPENDENCIANOMBRE, 
  TERCERO.NOMBRE,
  TRIM(NOMBRELARGO)||' -'||DEVOLUTIVO.DESCRIPCION AS DEVOLUTIVODESCRIPCION,
  TO_NUMBER(NVL(DEVOLUTIVO.VALOR,0))             AS VALORDEVOLUTIVO,
  PCK_ALMACEN.FC_NOMBREINVENTARIO(s$compania$s,SUBSTR(CodigoElemento,1,s$parametroAgrupacion$s),0) NOMBREAGRUPACION,

  ESTADODEVOLUTIVO.DESCRIPCION,
  SUBSTR(CodigoElemento,1,s$parametroAgrupacion$s) AS Agrupacion,
  DEVOLUTIVO.DEPENDENCIA,
 TO_CHAR( DEVOLUTIVO.FECHAADQUISICION
,'DD/MM/YYYY') FECHAADQUISICION
FROM ((((DEVOLUTIVO
LEFT JOIN INVENTARIO
ON (DEVOLUTIVO.COMPANIA  = INVENTARIO.COMPANIA)
AND (DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO))
LEFT JOIN DEPENDENCIA
ON (DEVOLUTIVO.COMPANIA     = DEPENDENCIA.COMPANIA)
AND (DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO))
LEFT JOIN RESPONSABLE
ON (DEVOLUTIVO.COMPANIA     = RESPONSABLE.COMPANIA)
AND (DEVOLUTIVO.RESPONSABLE = RESPONSABLE.CEDULA))
LEFT JOIN COMPANIA
ON DEVOLUTIVO.COMPANIA = COMPANIA.CODIGO)
LEFT JOIN ESTADODEVOLUTIVO
ON DEVOLUTIVO.ESTADO         = ESTADODEVOLUTIVO.ESTADO
LEFT JOIN TERCERO
ON (DEVOLUTIVO.COMPANIA     = TERCERO.COMPANIA)
AND (DEVOLUTIVO.RESPONSABLE = TERCERO.NIT)
AND DEVOLUTIVO.SUCURSAL_RESPONSABLE =TERCERO.SUCURSAL
WHERE DEVOLUTIVO.COMPANIA=s$compania$s
AND DEVOLUTIVO.FECHAADQUISICION BETWEEN s$fechaInicial$s AND s$fechaFinal$s
