SELECT  
      V.COMPANIA,  
      V.TIPOMOVIMIENTO, 
      V.MOVIMIENTO, 
      V.ELEMENTO  CODIGOELEMENTO,  
      V.ESPECIFICACION,   
      V.TIPOMOVIMIENTO_NOM TIPOMOV,  
      V.NOMBRELARGO,  
      V.UNIDAD,  
      V.CANTIDAD,  
      V.VALORUNITARIO, 
      V.VALORTOTAL_D VALORTOTAL, 
      TO_CHAR(V.FECHA_MOV,'DD/MM/YYYY') FECHA,  
      V.TERCERO,  
      V.RESPONSABLE_DESTINO CEDULA, 
      CENTRODECOSTO_NOM NOMBRECENTROCOSTO, 
      CENTRODECOSTO, 
      V.MOVASOCIADO,  
      V.FECHAMOVASOCIADO, 
      V.DEPENDENCIA_DESTINO DEPENDENCIA,  
      V.DESCRIPCION_MOV DESCRIPCION, 
      V.DEPENDENCIA_DESTINO_NOM NOMBREDEPENDENCIA,  
      V.TERCERO_NOM NOMBRERESPONSABLE,  
      V.TERCERO_TEL TELEFONOS,  
      V.TERCERO_FAX FAX,  
      V.TERCERO_DIR DIRECCION,  
      V.UNIDAD_NOM NOMBREUNIDAD,  
      V.AJUSTECENTAVOS1_MOV AJUSTECENTAVOS1,  
      V.TOTALCONAJUSTE_MOV TOTALCONAJUSTE, 
      V.TIPOMOVIMIENTO || MOVIMIENTO AS GRUPO,  
      V.TIPOPESONA TIPOPERSONA, 
      V.AJUSTECENTAVOS1_MOV*-1 AS AJUSTECENTAVOSM, 
      V.AJUSTECENTAVOS_D AS DETALLEAJUSTE,  
      V.AJUSTE_DEC_MANUAL, 
      V.TIPOMOVASOCIADO_NOM NOMBREDOCASOCIADO, 
      V.ESCRITURA, 
      V.NUMEROCATASTRAL,  
      V.MATINMOBILIARIA, 
      V.COD_CHIP, 
      V.RESPONSABLE_ORIGEN_NOM,
      V.RESPONSABLE_ORIGEN_CARGO,
      R.NUMERO RESERVA, 
      CASE WHEN  TRIM(V.IND_REG) IS NULL THEN 'NONUM' END AS NOREGISTRADO, 
      CASE TRIM(V.SERIE) 
           WHEN '' THEN NULL  
           ELSE V.SERIE  
      END SERIE, 
      CASE TRIM(V.MARCA) 
           WHEN '' THEN NULL 
           ELSE V.MARCA  
      END AS MARCA, 
      CASE V.CLASE WHEN 'E' THEN 'ENTRADA' ELSE 'SALIDA' END CLASEMOV, 
      CASE TRIM(V.SERIEDEVOLUTIVO) 
           WHEN '' THEN NULL 
       	   ELSE V.SERIEDEVOLUTIVO 
      END AS SERIEDEVOLUTIVO, 
      CASE V.TIPOPESONA 
      		WHEN 'R' THEN V.TERCERO_NOM || ', C.C.' || V.TERCERO  
        	ELSE V.TERCERO_NOM || ', Tel:' || V.TERCERO_TEL || ', Nit: ' ||  V.TERCERO 
       END || 
      CASE WHEN TRIM(V.TERCERO_DIR) IS NULL OR TRIM(V.TERCERO_DIR) = ' '  
      	   THEN ' '  
           ELSE ' Dirección: ' || V.TERCERO_DIR || ' ' 
       END || 
      CASE WHEN TRIM(V.TERCERO_FAX) IS NULL OR TRIM(V.TERCERO_FAX) = ' ' 
           THEN ' ' 
           ELSE ' Fax: ' || V.TERCERO_FAX 
       END INFPERSONA, 
      V.NOMBRELARGO ||  ' ' || 
      CASE TRIM(V.ESPECIFICACION) 
           WHEN NULL THEN ' '  
           ELSE V.ESPECIFICACION END || 
      CASE WHEN TRIM(V.MARCA) IS NOT NULL OR TRIM(V.MARCA) NOT IN ('')
      	   THEN ', MARCA ' || V.MARCA || '' 
           ELSE ' ' 
       END ||  
      CASE WHEN TRIM(V.SERIEDEVOLUTIVO) IS NULL OR V.SERIEDEVOLUTIVO = ' ' 
           THEN ' '  
           ELSE ', Serie: ' || V.SERIEDEVOLUTIVO END || 
      CASE WHEN TRIM(V.ESCRITURA) IS NULL  
           THEN ' ' 
           ELSE 'Escritura: ' || V.ESCRITURA END ||  
      CASE WHEN TRIM(V.NUMEROCATASTRAL) IS  NULL 
           THEN ' ' 
           ELSE 'No. Catastro: ' || V.NUMEROCATASTRAL END ||  
      CASE WHEN TRIM(V.MATINMOBILIARIA) IS NULL 
           THEN ' ' 
           ELSE 'Matrícula: ' || V.MATINMOBILIARIA 
           END ||  
      CASE WHEN TRIM(V.COD_CHIP) IS NULL 
           THEN ' ' 
           ELSE 'Chip: ' || V.COD_CHIP END DESCRELEMENTO 
FROM V_MOVIMIENTO_INFORME V 
LEFT JOIN V_ORDENDECOMPRA_RES R 
  ON V.COMPANIA       = R.COMPANIA 
 AND V.TIPOMOVIMIENTO = R.TIPO 
WHERE V.COMPANIA       = s$compania$s 
  AND V.TIPOMOVIMIENTO = 's$tipoMovimiento$s' 
  AND V.MOVIMIENTO BETWEEN s$movimientoInicial$s AND s$movimientoFinal$s 
ORDER BY V.COMPANIA
    , V.TIPOMOVIMIENTO
    , V.MOVIMIENTO
    , V.ELEMENTO
