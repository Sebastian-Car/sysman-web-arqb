SELECT DISTINCT COMPANIA,
  TIPOMOVIMIENTO,
  MOVIMIENTO ENTRDEVOLUTIVO,
  LPAD(ELEMENTO,3) AS EXPR1,
  SUM(VALORTOTAL)  AS SUMADEVALORTOTAL
FROM D_MOVIMIENTO
WHERE COMPANIA       = '$P!{PR_COMPANIA}'
  AND TIPOMOVIMIENTO = '$P!{PR_TIPOMOVIMIENTO}' 
  AND MOVIMIENTO     = $P!{PR_ENTRDEVOLUTIVO}
GROUP BY COMPANIA,
  TIPOMOVIMIENTO,
  MOVIMIENTO,
  LPAD(ELEMENTO,3)
