SELECT V_MOVIMIENTO_INFORME.COMPANIA,
       V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO,
       V_MOVIMIENTO_INFORME.MOVIMIENTO NUMERO,
       V_MOVIMIENTO_INFORME.CODIGO,
       V_MOVIMIENTO_INFORME.ELEMENTO,
       V_MOVIMIENTO_INFORME.SERIEDEVOLUTIVO,
       V_MOVIMIENTO_INFORME.SERIE,
       V_MOVIMIENTO_INFORME.ESPECIFICACION,
       CASE WHEN V_MOVIMIENTO_INFORME.IND_REG NOT IN (0) THEN ' ' ELSE 'X' END NOREGISTRADO, 
       TRIM(V_MOVIMIENTO_INFORME.MARCA) MARCA, 
       V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO_NOM NOMBRE_TIPO, 
       V_MOVIMIENTO_INFORME.TIPOPESONA, 
       V_MOVIMIENTO_INFORME.NOMBRELARGO, 
       V_MOVIMIENTO_INFORME.CANTIDAD, 
       V_MOVIMIENTO_INFORME.VALORUNITARIO, 
       V_MOVIMIENTO_INFORME.VALORTOTAL_D VALORTOTAL, 
       V_MOVIMIENTO_INFORME.TERCERO, 
       V_MOVIMIENTO_INFORME.FECHA_D_MOV FECHA, 
       V_MOVIMIENTO_INFORME.TIPOMOVASOCIADO, 
       V_MOVIMIENTO_INFORME.MOVASOCIADO, 
       V_MOVIMIENTO_INFORME.FECHAMOVASOCIADO, 
       V_MOVIMIENTO_INFORME.DEPENDENCIA_ORIGEN, 
       V_MOVIMIENTO_INFORME.DEPENDENCIA_ORIGEN_NOM,
       V_MOVIMIENTO_INFORME.DEPENDENCIA_DESTINO, 
       V_MOVIMIENTO_INFORME.DEPENDENCIA_DESTINO_NOM,
       V_MOVIMIENTO_INFORME.FACTURANO, 
       V_MOVIMIENTO_INFORME.REMISIONNO, 
       V_MOVIMIENTO_INFORME.TERCERO_NOM, 
       V_MOVIMIENTO_INFORME.TIPOMOVASOCIADO_NOM,
       V_MOVIMIENTO_INFORME.TERCERO_TEL TELEFONOS, 
       V_MOVIMIENTO_INFORME.TERCERO_DIR DIRECCION, 
       V_MOVIMIENTO_INFORME.TERCERO_FAX FAX,  
       V_MOVIMIENTO_INFORME.UNIDAD_NOM UNIDAD, 
       V_MOVIMIENTO_INFORME.AJUSTECENTAVOS1_MOV AJUSTECENTAVOS1, 
       V_MOVIMIENTO_INFORME.TOTALCONAJUSTE_MOV TOTALCONAJUSTE, 
       V_MOVIMIENTO_INFORME.CLASE_NOM CLASEMOV, 
       V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO || V_MOVIMIENTO_INFORME.MOVIMIENTO GRUPO, 
       V_MOVIMIENTO_INFORME.PORCDESCUENTO,
       V_MOVIMIENTO_INFORME.VALORUNITARIO * V_MOVIMIENTO_INFORME.PORCDESCUENTO/100 VALORTOTALDESC, 
       V_MOVIMIENTO_INFORME.AJUSTECENTAVOS1_MOV * (-1) AJUSTECENTAVOS,
       V_MOVIMIENTO_INFORME.AJUSTECENTAVOS_MOV AJU,
       V_MOVIMIENTO_INFORME.AJUSTECENTAVOS_D AJUSTEDETALLE,
       V_MOVIMIENTO_INFORME.AJUSTE_DEC_MANUAL,
       V_MOVIMIENTO_INFORME.ESCRITURA,
       V_MOVIMIENTO_INFORME.NUMEROCATASTRAL,
       V_MOVIMIENTO_INFORME.MATINMOBILIARIA,
       V_MOVIMIENTO_INFORME.COD_CHIP,
       V_MOVIMIENTO_INFORME.DESCRIPCION_MOV DESCRIPCION,
       (CASE WHEN V_MOVIMIENTO_INFORME.TIPOPESONA = 'R' 
  			THEN V_MOVIMIENTO_INFORME.RESPONSABLE_ORIGEN_NOM || ', C.C.' || V_MOVIMIENTO_INFORME.RESPONSABLE_ORIGEN 
  			ELSE V_MOVIMIENTO_INFORME.TERCERO_NOM || ', TEL:' || V_MOVIMIENTO_INFORME.TERCERO_TEL || ' , NIT: ' || V_MOVIMIENTO_INFORME.TERCERO END) || 
       (CASE WHEN (V_MOVIMIENTO_INFORME.TERCERO_DIR IS NULL) 
  			THEN ' ' 
  			ELSE '  DIRECCION:' || V_MOVIMIENTO_INFORME.TERCERO_DIR END) DESCRIPCION_INFORME
,
      (V_MOVIMIENTO_INFORME.NOMBRELARGO || 
        
        (CASE WHEN V_MOVIMIENTO_INFORME.MARCA IS NULL 
          THEN ' ' 
          ELSE ', MARCA ' || V_MOVIMIENTO_INFORME.MARCA END) || 
          
        (CASE WHEN V_MOVIMIENTO_INFORME.ESPECIFICACION IS NULL 
          THEN ' ' 
          ELSE V_MOVIMIENTO_INFORME.ESPECIFICACION END) ||
          
        (CASE WHEN V_MOVIMIENTO_INFORME.SERIEDEVOLUTIVO IS NULL 
          THEN ' ' 
          ELSE ' ,SERIE: ' || V_MOVIMIENTO_INFORME.SERIEDEVOLUTIVO END) || 
          
        (CASE WHEN V_MOVIMIENTO_INFORME.SERIE IN (0) 
OR V_MOVIMIENTO_INFORME.SERIE IS NULL
          THEN ' ' 
          ELSE ', PLACA:' || SERIE END)) DESCRIPCION_COLUMNA

FROM V_MOVIMIENTO_INFORME
WHERE V_MOVIMIENTO_INFORME.COMPANIA      = s$compania$s
AND V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO  = 's$tipoMovimiento$s'
AND V_MOVIMIENTO_INFORME.MOVIMIENTO BETWEEN s$movimientoInicial$s AND s$movimientoFinal$s
ORDER BY 
V_MOVIMIENTO_INFORME.MOVIMIENTO,
V_MOVIMIENTO_INFORME.CODIGO,
V_MOVIMIENTO_INFORME.ELEMENTO
