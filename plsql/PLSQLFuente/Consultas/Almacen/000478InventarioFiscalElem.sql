SELECT DEVOLUTIVO.ELEMENTO CODIGOELEMENTO,
	DEVOLUTIVO.SERIE,
	DEVOLUTIVO.VALOR VALOR,
	DEVOLUTIVO.VALOR VLR,
	DEVOLUTIVO.DEPENDENCIA DEPENDENCIA,
	DEPENDENCIA.NOMBRE,
	ELEMENTO,
	DEVOLUTIVO.MARCA,
	INVENTARIO.NOMBRELARGO||' '||DEVOLUTIVO.DESCRIPCION||''||CASE WHEN TRIM(DEVOLUTIVO.MARCA) IS NULL OR DEVOLUTIVO.MARCA = ' ' THEN ' ' ELSE ' Marca: '||DEVOLUTIVO.MARCA END ||' '||CASE WHEN TRIM(SerieDevolutivo) IS NULL THEN ' ' ELSE ' Serie: '||SERIEDEVOLUTIVO END  NOMBRE_DESCRIPCION
FROM DEVOLUTIVO  
  LEFT JOIN DEPENDENCIA ON DEVOLUTIVO.COMPANIA = DEPENDENCIA.COMPANIA 
    AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO
  INNER JOIN INVENTARIO ON DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA
    AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
WHERE DEVOLUTIVO.COMPANIA=s$compania$s
AND DEVOLUTIVO.DEPENDENCIA BETWEEN 's$depInicial$s' AND 's$depFinal$s' 
AND DEPENDENCIA.CLASE_BODEGA NOT IN (40,50,90) 
AND DEVOLUTIVO.PLACAANULADA=0
