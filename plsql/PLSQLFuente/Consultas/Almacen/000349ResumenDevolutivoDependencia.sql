SELECT DEVOLUTIVO.COMPANIA, 
  DEPENDENCIA.NOMBRE, 
  DEVOLUTIVO.VALOR AS VALORDEVOLUTIVO, 
  ESTADODEVOLUTIVO.DESCRIPCION, 
  LPAD(INVENTARIO.CODIGOELEMENTO,3) AS AGRUPACION, 
  DEVOLUTIVO.DEPENDENCIA,
  INVENTARIO_1.NOMBRELARGO AS NOMBRELARGO
FROM INVENTARIO  INVENTARIO_1 
  INNER JOIN
    DEVOLUTIVO
  INNER JOIN INVENTARIO
    ON DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA
    AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO 
  INNER JOIN DEPENDENCIA 
    ON DEVOLUTIVO.COMPANIA = DEPENDENCIA.COMPANIA
    AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO 
  INNER JOIN TERCERO ON DEVOLUTIVO.COMPANIA = TERCERO.COMPANIA 
    AND DEVOLUTIVO.RESPONSABLE = TERCERO.NIT
    AND DEVOLUTIVO.SUCURSAL_RESPONSABLE = TERCERO.SUCURSAL
  INNER JOIN ESTADODEVOLUTIVO ON DEVOLUTIVO.ESTADO = ESTADODEVOLUTIVO.ESTADO
    ON INVENTARIO_1.COMPANIA = INVENTARIO.COMPANIA AND INVENTARIO_1.CODIGOELEMENTO = LPAD(INVENTARIO.CODIGOELEMENTO,3)
WHERE DEVOLUTIVO.COMPANIA =  s$compania$s
  AND LPAD(INVENTARIO.CODIGOELEMENTO,3) 
    BETWEEN   s$elementoDesde$s AND s$elementoHasta$s
      AND DEVOLUTIVO.DEPENDENCIA 
    BETWEEN s$codigoInicial$s
      AND s$codigoFinal$s  
  AND DEVOLUTIVO.PLACAANULADA=0
ORDER BY INVENTARIO.NOMBRELARGO
