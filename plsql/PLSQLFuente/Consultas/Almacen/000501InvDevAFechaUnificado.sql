WITH ULTIMOMOVDEVFECHAFIN AS( 
    SELECT D_MOVIMIENTO.COMPANIA ,
    D_MOVIMIENTO.ELEMENTO, 
    D_MOVIMIENTO.SERIE, 
    MAX(MOVIMIENTO.DEPENDENCIA_DESTINO)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS ULTIMODEDEPENDENCIA1,
    MAX(TO_CHAR(D_MOVIMIENTO.FECHA,'YYYYMMDD'))  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS FECHAX,
    MAX(D_MOVIMIENTO.FECHA)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS ULTIMODEFECHA 
    FROM D_MOVIMIENTO INNER JOIN V_MOVIMIENTO MOVIMIENTO ON 
    (D_MOVIMIENTO.COMPANIA = MOVIMIENTO.COMPANIA) AND 
    (D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO) AND
    (D_MOVIMIENTO.MOVIMIENTO = MOVIMIENTO.NUMERO)
    WHERE D_MOVIMIENTO.COMPANIA=s$compania$s 
    AND D_MOVIMIENTO.ELEMENTO
    BETWEEN s$ELEMENTODESDE$s AND s$ELEMENTOHASTA$s 
    AND D_MOVIMIENTO.FECHA<= s$FECHA$s 
    AND (D_MOVIMIENTO.IND_REG NOT IN (0))
    GROUP BY D_MOVIMIENTO.COMPANIA,
    D_MOVIMIENTO.ELEMENTO,
    D_MOVIMIENTO.SERIE
    ORDER BY D_MOVIMIENTO.COMPANIA DESC,
    D_MOVIMIENTO.ELEMENTO DESC)
                
SELECT INVENTARIO.COMPANIA, 
	INVENTARIO.CODIGOELEMENTO, 
	INVENTARIO.NOMBRELARGO, 
	s$ORDEN2$s AS ORDEN2, 
	s$ORDEN3$s AS ORDEN3, 
	DEVOLUTIVO.SERIE AS PLK,
	ULTIMOMOVDEVFECHAFIN.ULTIMODEDEPENDENCIA1 AS DEPENDENCIA, 
	INVENTARIO.TIPO,
	INVENTARIO.MARCA, 
	INVENTARIO.LOCALIZACION,
	SUBSTR(CODIGOELEMENTO,0,s$PR_DIGITOSAGRUPACIONINV$s) AS SIG,
	PCK_ALMACEN.FC_NOMBREINVENTARIO(INVENTARIO.COMPANIA,SUBSTR(CODIGOELEMENTO,0,s$PR_DIGITOSAGRUPACIONINV$s),0) AS NOMBREINVENTARIO,
	INVENTARIO.CANTRESERVADA,
	INVENTARIO.CANTCOMPORLLEGAR, 
	INVENTARIO.CANTINSERVIBLE,
	DEVOLUTIVO.VALOR AS VALORTOTAL,
	LENGTH(CODIGOELEMENTO) AS EXPR1, 
	1 AS TCANTIDAD, 
	DEVOLUTIVO.VALOR,
        TO_CHAR(ULTIMOMOVDEVFECHAFIN.ULTIMODEFECHA,'DD/MM/YYYY') ULTIMODEFECHA, 
	DEVOLUTIVO.PLACAANULADA
FROM INVENTARIO INNER JOIN 
	ULTIMOMOVDEVFECHAFIN ON 
	INVENTARIO.COMPANIA = ULTIMOMOVDEVFECHAFIN.COMPANIA AND 
	INVENTARIO.CODIGOELEMENTO = ULTIMOMOVDEVFECHAFIN.ELEMENTO
	INNER JOIN DEVOLUTIVO ON
	ULTIMOMOVDEVFECHAFIN.COMPANIA = DEVOLUTIVO.COMPANIA AND
	ULTIMOMOVDEVFECHAFIN.ELEMENTO = DEVOLUTIVO.ELEMENTO AND
	ULTIMOMOVDEVFECHAFIN.SERIE = DEVOLUTIVO.SERIE
WHERE ULTIMOMOVDEVFECHAFIN.ULTIMODEDEPENDENCIA1
BETWEEN s$DEPENDENCIAINICIAL$s AND s$DEPENDENCIAFINAL$s 
 AND INVENTARIO.TIPO NOT IN ('C') 
 AND LENGTH(CODIGOELEMENTO)>=s$PR_DIGITOSAGRUPACIONINV$s
 AND ULTIMOMOVDEVFECHAFIN.ULTIMODEFECHA<=s$FECHA$s 
 AND DEVOLUTIVO.PLACAANULADA IN (0)
 AND INVENTARIO.TIENEMOVIMIENTO NOT IN (0)
 ORDER BY INVENTARIO.CODIGOELEMENTO