SELECT E.CONSECUTIVO COMPONENTECODIGO,
  E.NOMBRE COMPONENTENOMBRE, DEVOLUTIVO.MESESVIDAUTILPLACA VIDAUTILCOMPONENTE,
  DEPRECIAR.ELEMENTO,
 
  DEPRECIAR.SERIE,
  INVENTARIO.NOMBRELARGO
  ||' '
  ||DEVOLUTIVO.DESCRIPCION
  ||
  CASE
    WHEN TRIM(DEVOLUTIVO.MARCA) IS NULL
    THEN ''
    ELSE ' Marca: '
      ||DEVOLUTIVO.MARCA
  END NOMBRELARGOS,
  DEPRECIAR.NIIF_VLRELEMENTO,
  DEPRECIAR.NIIF_COSTOAJUSTADO,
  DEPRECIAR.NIIF_VLRDEPRECIACION,
  DEPRECIAR.NIIF_DEPACUMULADA,
  DEPRECIAR.NIIF_VLRLIBROS,
  DEPRECIAR.PERIODO,
  NI.CUENTACREDITO CODIGOCUENTA,
  PLAN_CONTABLE.NOMBRE NOMBRECUENTA,
  CASE WHEN D.MATERIAL NOT IN (0) THEN 'SIGNIFICATIVO' ELSE 'NO SIGNIFICATIVO' END MATERIAL 
FROM DEPRECIAR
LEFT JOIN DEVOLUTIVO
ON DEPRECIAR.COMPANIA  = DEVOLUTIVO.COMPANIA
AND DEPRECIAR.ELEMENTO = DEVOLUTIVO.ELEMENTO
AND DEPRECIAR.SERIE    = DEVOLUTIVO.SERIE
INNER JOIN DETALLE_ELEMENTOSCOMPONENTES D
ON DEVOLUTIVO.COMPANIA  = D.COMPANIA
AND DEVOLUTIVO.ELEMENTO = D.CODIGO_COMPONENTE
AND DEVOLUTIVO.SERIE    = D.SERIE_COMPONENTE
INNER JOIN ELEMENTOSCOMPONENTES E
ON D.COMPANIA         = E.COMPANIA
AND D.CODIGO_ELEMENTO = E.CONSECUTIVO
LEFT JOIN INVENTARIO
ON DEPRECIAR.COMPANIA  = INVENTARIO.COMPANIA
AND DEPRECIAR.ELEMENTO = INVENTARIO.CODIGOELEMENTO
LEFT JOIN NIIF_INVENTARIOCONTA NI
ON DEPRECIAR.COMPANIA          = NI.COMPANIA
AND EXTRACT(YEAR FROM DEPRECIAR.PERIODO)        = NI.ANO
AND SUBSTR(DEPRECIAR.ELEMENTO,0, PCK_SYSMAN_UTL.FC_PAR(DEVOLUTIVO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO','10',SYSDATE,-1)) = NI.CODIGOELEMENTO 
AND DEPRECIAR.NIIF_TIPO_ACTIVO = NI.TIPOACTIVO
AND DEPRECIAR.BODEGA           = NI.BODEGA
LEFT JOIN PLAN_CONTABLE
ON NI.COMPANIA          =PLAN_CONTABLE.COMPANIA
AND NI.ANO              =PLAN_CONTABLE.ANO
AND NI.CUENTACREDITO    =PLAN_CONTABLE.CODIGO
WHERE DEPRECIAR.COMPANIA=s$compania$s
  AND E.CONSECUTIVO BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s' 
  AND TO_CHAR(DEPRECIAR.PERIODO, 'DD/MM/YYYY')='s$ultimoDia$s'
  AND (TO_DATE(TO_CHAR(NVL(FECHAANULADA,TO_DATE('31/12/3000')), 'DD/MM/YYYY'), 'DD/MM/YYYY')>TO_DATE('s$ultimoDia$s', 'DD/MM/YYYY') OR FECHAANULADA IS NULL)
ORDER BY E.CONSECUTIVO, 
D.MATERIAL
