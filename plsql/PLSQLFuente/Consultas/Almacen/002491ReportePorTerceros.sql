MERGE INTO CONSULTAS FIN USING (SELECT '002491ReportePorTerceros' INFORME ,TO_CLOB(q'[SELECT M.TIPOMOVIMIENTO, M.NUMERO EGRESO,  
TO_CHAR(M.FECHA, 'YYYY') VIGENCIA_EGRESO, 
TO_CHAR(M.FECHA, 'DD/MM/YYYY') FECHAEGRESO, 
M.CODIGOPROYECTO, 
M.DEPENDENCIA_DESTINO DEPENDENCIA, 
M.AUXILIAR, 
A.NOMBRE NOMBRE_AUXILIAR, 
OCP.RUBRO,
SUM(D.VALORTOTAL) VALOR,
AFECTADO.TERCERO PROVEEDOR, T.NOMBRE NOMBRE_PROVEEDOR
FROM MOVIMIENTO M
LEFT JOIN AUXILIAR A
 ON M.COMPANIA = A.COMPANIA
AND M.ANO = A.ANO
AND M.AUXILIAR = A.CODIGO
INNER JOIN D_MOVIMIENTO D
 ON M.COMPANIA = D.COMPANIA
AND M.TIPOMOVIMIENTO = D.TIPOMOVIMIENTO
AND M.NUMERO = D.MOVIMIENTO
LEFT JOIN MOVIMIENTO AFECTADO
 ON D.COMPANIA = AFECTADO.COMPANIA
AND D.TIPOMOVIMIENTO_AFECT = AFECTADO.TIPOMOVIMIENTO
AND D.MOVIMIENTO_AFECT = AFECTADO.NUMERO
LEFT JOIN ORDENDECOMPRA OC
 ON AFECTADO.COMPANIA = OC.COMPANIA
AND AFECTADO.TIPOMOVASOCIADO = OC.CLASEORDEN
AND AFECTADO.MOVASOCIADO = OC.NUMERO
LEFT JOIN TERCERO T
 ON AFECTADO.COMPANIA = T.COMPANIA
AND AFECTADO.TERCERO = T.NIT
AND AFECTADO.SUCURSAL = T.SUCURSAL
LEFT JOIN V_ORDENDECOMPRA_RUBRO OCP
 ON OC.COMPANIA = OCP.COMPANIA
AND OC.CLASEORDEN = OCP.CLASEORDEN
AND OC.NUMERO = OCP.NUMERO
INNER JOIN TIPOMOVIMIENTO TM
 ON M.COMPANIA = TM.COMPANIA
AND M.TIPOMOVIMIENTO = TM.CODIGO
WHERE M.COMPANIA = s$compania$s
AND M.FECHA BETWEEN s$fechaIni$s AND s$fechaFin$s
AND M.DEPENDENCIA_DESTINO BETWEEN 's$dependenciaIni$s' AND 's$dependenciaFin$s'
AND M.CODIGOPROYECTO BETWEEN 's$proyectoIni$s' AND 's$proyectoFin$s'
AND TM.TIPOELEMENTO IN ('C')
AND TM.CLASE = 'S'
GROUP BY M.TIPOMOVIMIENTO, M.NUMERO, TO_CHAR(M.FECHA, 'YYYY'), TO_CHAR(M.FECHA, 'DD/MM/YYYY'), M.CODIGOPROYECTO, 
M.DEPENDENCIA_DESTINO, M.AUXILIAR, A.NOMBRE, AFECTADO.TIPOMOVASOCIADO, AFECTADO.MOVASOCIADO, 
OCP.RUBRO, AFECTADO.TERCERO, T.NOMBRE]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);