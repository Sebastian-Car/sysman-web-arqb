MERGE INTO CONSULTAS FIN USING (SELECT '800709AuxiliarMovimientosTipoE' INFORME ,TO_CLOB(q'[SELECT 
  TO_CHAR(MOVIMIENTO.FECHA, 'DD/MM/YYYY') AS FECHA,
  DEPENDENCIA.NOMBRE NOMBREDEPENDENCIA,
  TERCERO.NOMBRE NOMBRERESPONSABLE,
  MOVIMIENTO.TIPOMOVIMIENTO,
  MOVIMIENTO.NUMERO,
  D_MOVIMIENTO.ELEMENTO,
  INVENTARIO.NOMBRELARGO,
  D_MOVIMIENTO.SERIE,
  INVENTARIO.UNIDAD,
  D_MOVIMIENTO.CANTIDAD,
  D_MOVIMIENTO.VALORUNITARIO,
  D_MOVIMIENTO.VALORTOTAL,
  MOVIMIENTO.DESCRIPCION,
  MOVIMIENTO.TIPOMOVASOCIADO || ' - ' || MOVIMIENTO.MOVASOCIADO CONTRATO,
  MOVIMIENTO.FACTURANO FACTURA,
  D_MOVIMIENTO.REFERENCIA,
  D_MOVIMIENTO.LOTE,
  TO_CHAR(D_MOVIMIENTO.FECHA_VENCIMIENTO,'DD/MM/YYYY') FECHA_VENCIMIENTO,
  D_MOVIMIENTO.FUENTEDERECURSO
FROM
  MOVIMIENTO 
  INNER JOIN TIPOMOVIMIENTO
    ON MOVIMIENTO.COMPANIA = TIPOMOVIMIENTO.COMPANIA
    AND MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
  INNER JOIN TERCERO 
    ON MOVIMIENTO.COMPANIA = TERCERO.COMPANIA
    AND MOVIMIENTO.TERCERO = TERCERO.NIT 
    AND MOVIMIENTO.SUCURSAL = TERCERO.SUCURSAL 
  INNER JOIN DEPENDENCIA 
    ON MOVIMIENTO.COMPANIA = DEPENDENCIA.COMPANIA
    AND MOVIMIENTO.DEPENDENCIA_DESTINO = DEPENDENCIA.CODIGO
  LEFT JOIN D_MOVIMIENTO 
    ON MOVIMIENTO.COMPANIA = D_MOVIMIENTO.COMPANIA  
    AND MOVIMIENTO.TIPOMOVIMIENTO = D_MOVIMIENTO.TIPOMOVIMIENTO
    AND MOVIMIENTO.NUMERO = D_MOVIMIENTO.MOVIMIENTO
  INNER JOIN INVENTARIO 
    ON D_MOVIMIENTO.COMPANIA = INVENTARIO.COMPANIA
    AND D_MOVIMIENTO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
WHERE MOVIMIENTO.COMPANIA = s$compania$s  
  AND MOVIMIENTO.TIPOMOVIMIENTO BETWEEN 's$tipoInicial$s' AND 's$tipoFinal$s'
  AND MOVIMIENTO.FECHA BETWEEN TO_DATE('s$desde$s','dd/MM/yyyy') AND TO_DATE('s$hasta$s','dd/MM/yyyy')
ORDER BY MOVIMIENTO.TIPOMOVIMIENTO, MOVIMIENTO.NUMERO]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);