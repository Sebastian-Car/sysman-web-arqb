SELECT 
      V_MOVIMIENTO_INFORME.COMPANIA, 
      V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO,
      V_MOVIMIENTO_INFORME.MOVIMIENTO,
      V_MOVIMIENTO_INFORME.ELEMENTO AS CODIGOELEMENTO, 
      V_MOVIMIENTO_INFORME.ESPECIFICACION,  
      V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO_NOM TIPOMOV, 
      V_MOVIMIENTO_INFORME.NOMBRELARGO, 
      V_MOVIMIENTO_INFORME.UNIDAD, 
      V_MOVIMIENTO_INFORME.CANTIDAD, 
      V_MOVIMIENTO_INFORME.VALORUNITARIO,
      V_MOVIMIENTO_INFORME.VALORTOTAL_D VALORTOTAL,
      V_MOVIMIENTO_INFORME.VALORTOTAL_MOV AS VLRTOTAL,
      V_MOVIMIENTO_INFORME.FECHA_MOV FECHA, 
      V_MOVIMIENTO_INFORME.TERCERO, 
      V_MOVIMIENTO_INFORME.MOVASOCIADO,
      V_MOVIMIENTO_INFORME.TIPOMOVASOCIADO,
      V_MOVIMIENTO_INFORME.FECHAMOVASOCIADO,
      V_MOVIMIENTO_INFORME.DEPENDENCIA_DESTINO DEPENDENCIA, 
      V_MOVIMIENTO_INFORME.DESCRIPCION_MOV DESCRIPCION, 
      V_MOVIMIENTO_INFORME.DEPENDENCIA_DESTINO_NOM NOMBREDEPENDENCIA, 
      V_MOVIMIENTO_INFORME.TERCERO_TEL TELEFONOS, 
      V_MOVIMIENTO_INFORME.TERCERO_FAX FAX, 
      V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO CEDULA, 
      V_MOVIMIENTO_INFORME.CENTRODECOSTO_NOM NOMBRECENTROCOSTO,
      V_MOVIMIENTO_INFORME.CENTRODECOSTO,
      NVL(V_MOVIMIENTO_INFORME.VALORDOCASOCIADO,0) AS VALORDOCASOCIADO, 
      V_MOVIMIENTO_INFORME.UNIDAD_NOM NOMBREUNIDAD, 
      V_MOVIMIENTO_INFORME.AJUSTECENTAVOS1_MOV AJUSTECENTAVOS1, 
      V_MOVIMIENTO_INFORME.TOTALCONAJUSTE_MOV TOTALCONAJUSTE,
      V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO_CARGO AS CARGORESPONSABLE, 
      V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO || V_MOVIMIENTO_INFORME.MOVIMIENTO AS GRUPO, 
      V_MOVIMIENTO_INFORME.TIPOPESONA TIPOPERSONA,
      V_MOVIMIENTO_INFORME.AJUSTECENTAVOS1_MOV AS AJUSTECENTAVOS,
      V_MOVIMIENTO_INFORME.AJUSTECENTAVOS1_MOV AJUSTECENTAVOS1, 
      V_MOVIMIENTO_INFORME.AJUSTECENTAVOS_D AS DETALLEAJUSTE, 
      V_MOVIMIENTO_INFORME.AJUSTE_DEC_MANUAL,
      V_MOVIMIENTO_INFORME.TIPOMOVASOCIADO_NOM NOMBREDOCASOCIADO,
      CASE WHEN  TRIM(V_MOVIMIENTO_INFORME.IND_REG) IS NULL THEN 'NONUM' END AS NOREGISTRADO,
      CASE TRIM(V_MOVIMIENTO_INFORME.SERIE) WHEN '' THEN NULL 
        ELSE V_MOVIMIENTO_INFORME.SERIE 
      END SERIE,
      CASE TRIM(V_MOVIMIENTO_INFORME.MARCA) WHEN '' THEN NULL
        ELSE V_MOVIMIENTO_INFORME.MARCA 
      END AS MARCA,
      CASE TRIM(V_MOVIMIENTO_INFORME.SERIEDEVOLUTIVO) WHEN '' THEN NULL
       ELSE V_MOVIMIENTO_INFORME.SERIEDEVOLUTIVO END AS SERIEDEVOLUTIVO,
      CASE V_MOVIMIENTO_INFORME.TIPOPESONA WHEN 'R' THEN V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO_NOM || ', C.C.' || 
           V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO ELSE V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO || ', Tel:' ||  TERCERO.TELEFONOS
          || ' , Nit: ' || V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO END || CASE  WHEN TRIM(NVL(TERCERO.FAX,'')) IS NOT NULL THEN 'Fax: ' || TERCERO.FAX || ' '
        ELSE ''
       END RESPONSABLE,
	    NVL(V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO_NOM,' ') NOMBRERESPONSABLE,
      V_MOVIMIENTO_INFORME.NOMBRELARGO || ' ' ||  
      CASE  WHEN TRIM(NVL(V_MOVIMIENTO_INFORME.ESPECIFICACION,'')) IS NULL THEN ' ' 
        ELSE V_MOVIMIENTO_INFORME.ESPECIFICACION 
       END || 
      CASE  WHEN TRIM(NVL(V_MOVIMIENTO_INFORME.MARCA,'')) IS NOT NULL THEN 'MARCA: ' || V_MOVIMIENTO_INFORME.MARCA || ' '
        ELSE ''
      END || 
      CASE  WHEN TRIM(NVL(V_MOVIMIENTO_INFORME.SERIEDEVOLUTIVO,'')) IS NOT NULL THEN ', SERIE: ' || V_MOVIMIENTO_INFORME.SERIEDEVOLUTIVO || ' ' 
        ELSE ''
      END ||
      CASE WHEN TRIM(NVL(V_MOVIMIENTO_INFORME.SERIE,''))= 0 THEN ','
        ELSE ', PLACA: ' || V_MOVIMIENTO_INFORME.SERIE
      END DESCRIPCIONELEMENTO
, 
     V_MOVIMIENTO_INFORME.NOMBRE_RECIBIDOPOR 
FROM V_MOVIMIENTO_INFORME LEFT JOIN TERCERO
       ON V_MOVIMIENTO_INFORME.COMPANIA = TERCERO.COMPANIA
      AND V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO = TERCERO.NIT
      AND V_MOVIMIENTO_INFORME.SUCURSAL_RESDESTINO = TERCERO.SUCURSAL
WHERE V_MOVIMIENTO_INFORME.COMPANIA       = s$compania$s
  AND V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO = 's$tipoMovimiento$s'
  AND V_MOVIMIENTO_INFORME.MOVIMIENTO BETWEEN s$movimientoInicial$s AND s$movimientoFinal$s
ORDER BY COMPANIA, TIPOMOVIMIENTO, MOVIMIENTO, ELEMENTO
