SELECT MOVINVENTARIO.COMPANIA, 
        MOVINVENTARIO.ELEMENTO ELEMENTO1,
        MOVINVENTARIO.SERIE,
        TRIM(INVENTARIO.NOMBRELARGO) || ' MARCA:' || DEVOLUTIVO.MARCA || ' - ' || DEVOLUTIVO.DESCRIPCION ||
        CASE WHEN TRIM(DEVOLUTIVO.SERIEDEVOLUTIVO) = ''
             THEN ''
             ELSE 'SERIE:' || DEVOLUTIVO.SERIEDEVOLUTIVO
              END NOMBRELARGO,
        MOVINVENTARIO.RESPONSABLE_NOMBRE, 
        MOVINVENTARIO.DEPENDENCIA_NOMBRE,        
        MOVINVENTARIO.VALORUNITARIO TVALOR, 
        DEVOLUTIVO.SERIEANTERIOR, 
        INVENTARIO.NOMBRELARGO NOMELEMENTO
   FROM  (SELECT MOVINVENTARIO.COMPANIA,
                 MOVINVENTARIO.ELEMENTO,
                 MOVINVENTARIO.SERIE,
                 MAX(MOVINVENTARIO.UFECHA) MAXIMO
           FROM (  SELECT D_MOVIMIENTO.COMPANIA,
                          D_MOVIMIENTO.ELEMENTO, 
                          D_MOVIMIENTO.SERIE,
                          TO_CHAR(D_MOVIMIENTO.FECHA,'DD/MM/YYYY') || TO_CHAR(D_MOVIMIENTO.HORA,'HH24:MI:SS') UFECHA
                     FROM V_MOVIMIENTO 
                    INNER JOIN D_MOVIMIENTO
                            ON V_MOVIMIENTO.COMPANIA       = D_MOVIMIENTO.COMPANIA
                           AND V_MOVIMIENTO.TIPOMOVIMIENTO = D_MOVIMIENTO.TIPOMOVIMIENTO 
                           AND V_MOVIMIENTO.NUMERO         = D_MOVIMIENTO.CODIGO
                    WHERE V_MOVIMIENTO.COMPANIA = s$compania$s
                      AND D_MOVIMIENTO.ELEMENTO BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s'
                      AND D_MOVIMIENTO.IND_REG NOT IN(0) 
                      AND D_MOVIMIENTO.FECHA <= s$fecha$s) MOVINVENTARIO
                    GROUP BY MOVINVENTARIO.COMPANIA, MOVINVENTARIO.ELEMENTO, MOVINVENTARIO.SERIE)
  MOVINVENTARIOAGRU 
  INNER JOIN  (  SELECT D_MOVIMIENTO.COMPANIA,
                        D_MOVIMIENTO.TIPOMOVIMIENTO,
                        D_MOVIMIENTO.ENTRDEVOLUTIVO MOVIMIENTO,
                        D_MOVIMIENTO.ELEMENTO, 
                        D_MOVIMIENTO.SERIE,
                        TO_CHAR(D_MOVIMIENTO.FECHA,'DD/MM/YYYY') || TO_CHAR(D_MOVIMIENTO.HORA,'HH24:MI:SS') UFECHA, 
                        D_MOVIMIENTO.FECHA,
                        D_MOVIMIENTO.HORA, 
                        D_MOVIMIENTO.RESPONSABLENUE RESPONSABLE, 
                        TERCERO.NOMBRE RESPONSABLE_NOMBRE,
                        D_MOVIMIENTO.DEPENDENCIANUE DEPENDENCIA,
                        DEPENDENCIA.NOMBRE DEPENDENCIA_NOMBRE,
                        D_MOVIMIENTO.VALORUNITARIO
                   FROM V_MOVIMIENTO 
                  INNER JOIN D_MOVIMIENTO
                     ON V_MOVIMIENTO.COMPANIA       = D_MOVIMIENTO.COMPANIA
                    AND V_MOVIMIENTO.TIPOMOVIMIENTO = D_MOVIMIENTO.TIPOMOVIMIENTO 
                    AND V_MOVIMIENTO.NUMERO         = D_MOVIMIENTO.CODIGO
                  INNER JOIN TERCERO
                     ON V_MOVIMIENTO.COMPANIA       = TERCERO.COMPANIA
                    AND V_MOVIMIENTO.TERCERO        = TERCERO.NIT
                    AND V_MOVIMIENTO.SUCURSAL       = TERCERO.SUCURSAL
                  INNER JOIN DEPENDENCIA
                     ON V_MOVIMIENTO.COMPANIA       = DEPENDENCIA.COMPANIA
                    AND V_MOVIMIENTO.DEPENDENCIA_DESTINO = DEPENDENCIA.CODIGO
                  WHERE V_MOVIMIENTO.COMPANIA = s$compania$s
                    AND D_MOVIMIENTO.ELEMENTO BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s'
                    AND D_MOVIMIENTO.IND_REG NOT IN(0) 
                    AND D_MOVIMIENTO.FECHA <= s$fecha$s) 
 MOVINVENTARIO 
          ON MOVINVENTARIOAGRU.COMPANIA = MOVINVENTARIO.COMPANIA
          AND MOVINVENTARIOAGRU.ELEMENTO = MOVINVENTARIO.ELEMENTO 
         AND MOVINVENTARIOAGRU.SERIE    = MOVINVENTARIO.SERIE 
         AND MOVINVENTARIOAGRU.MAXIMO   = MOVINVENTARIO.UFECHA          
   LEFT JOIN INVENTARIO 
          ON MOVINVENTARIOAGRU.COMPANIA = INVENTARIO.COMPANIA
         AND MOVINVENTARIOAGRU.ELEMENTO = INVENTARIO.CODIGOELEMENTO 
  INNER JOIN DEVOLUTIVO 
          ON MOVINVENTARIOAGRU.COMPANIA = DEVOLUTIVO.COMPANIA 
         AND MOVINVENTARIOAGRU.ELEMENTO = DEVOLUTIVO.ELEMENTO 
         AND MOVINVENTARIOAGRU.SERIE    = DEVOLUTIVO.SERIE
  WHERE ((NVL(PLACAANULADA,0)) IN (0)
         s$condBodegas$s)        
     OR ((NVL(PLACAANULADA,0)) NOT IN (0) 
         s$condBodegas$s
    AND DEVOLUTIVO.FECHAANULADA > s$fecha$s
)
   ORDER BY INVENTARIO.NOMBRELARGO 
