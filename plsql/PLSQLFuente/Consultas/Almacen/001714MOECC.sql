SELECT V_MOVIMIENTO_INFORME.COMPANIA,
       V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO,
       V_MOVIMIENTO_INFORME.MOVIMIENTO,
       V_MOVIMIENTO_INFORME.CODIGO,
       V_MOVIMIENTO_INFORME.ELEMENTO CODIGOELEMENTO,
       CASE WHEN V_MOVIMIENTO_INFORME.IND_REG NOT IN (0) THEN ' ' ELSE 'X' END NOREGISTRADO,
       V_MOVIMIENTO_INFORME.ESPECIFICACION, 
       NVL(TRIM(V_MOVIMIENTO_INFORME.SERIE),' ')  SERIE, 
       V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO_NOM, 
       V_MOVIMIENTO_INFORME.NOMBRELARGO, 
       V_MOVIMIENTO_INFORME.UNIDAD_NOM UNIDAD, 
       V_MOVIMIENTO_INFORME.CANTIDAD, 
       V_MOVIMIENTO_INFORME.VALORUNITARIO, 
       V_MOVIMIENTO_INFORME.VALORTOTAL_D VALORTOTAL,
       V_MOVIMIENTO_INFORME.FECHA_MOV FECHA,  
 	     NVL(V_MOVIMIENTO_INFORME.TIPOMOVASOCIADO,' ') TIPOMOVASOCIADO, 
       NVL(V_MOVIMIENTO_INFORME.MOVASOCIADO, 0) MOVASOCIADO, 
       V_MOVIMIENTO_INFORME.FECHAMOVASOCIADO, 
       V_MOVIMIENTO_INFORME.DESCRIPCION_MOV DESCRIPCION, 
       V_MOVIMIENTO_INFORME.VALORTOTAL_MOV VLRTOTAL, 
       V_MOVIMIENTO_INFORME.DEPENDENCIA_ORIGEN_NOM,
       V_MOVIMIENTO_INFORME.TIPOMOVASOCIADO_NOM, 
       NVL(V_MOVIMIENTO_INFORME.MARCA,' ') MARCA, 
       NVL(V_MOVIMIENTO_INFORME.SERIEDEVOLUTIVO,' ') SERIEDEVOLUTIVO,
       NVL(V_MOVIMIENTO_INFORME.VALORDOCASOCIADO,0)  VALORDOCASOCIADO, 
       V_MOVIMIENTO_INFORME.AJUSTECENTAVOS1_MOV AJUSTECENTAVOS1, 
       V_MOVIMIENTO_INFORME.TOTALCONAJUSTE_MOV TOTALCONAJUSTE, 
       V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO || V_MOVIMIENTO_INFORME.MOVIMIENTO  GRUPO, 
       V_MOVIMIENTO_INFORME.TIPOPESONA,
       V_MOVIMIENTO_INFORME.RESPONSABLE_ORIGEN_NOM,
       V_MOVIMIENTO_INFORME.AJUSTECENTAVOS1_MOV  AJUSTECENTAVOS,
       V_MOVIMIENTO_INFORME.AJUSTECENTAVOS_D  DETALLEAJUSTE,
       (CASE WHEN V_MOVIMIENTO_INFORME.TIPOPESONA IN ('R') 
          THEN V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO_NOM || ', C.C.' || V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO 
          ELSE V_MOVIMIENTO_INFORME.TERCERO_NOM || ', TEL:' || V_MOVIMIENTO_INFORME.TERCERO_TEL || ' , NIT: ' || V_MOVIMIENTO_INFORME.TERCERO END) || 
       (CASE WHEN V_MOVIMIENTO_INFORME.TERCERO_DIR IS NOT NULL 
          THEN '  DIRECCIÓN:' || V_MOVIMIENTO_INFORME.TERCERO_DIR || ' ' END) || 
       (CASE WHEN V_MOVIMIENTO_INFORME.TERCERO_FAX IS NOT NULL THEN '  FAX:' || V_MOVIMIENTO_INFORME.TERCERO_FAX END) DESCRIPCIONHEADER,
       NOMBRELARGO || ' ' || (CASE WHEN ESPECIFICACION IS NOT NULL THEN ESPECIFICACION END) || 
       (CASE WHEN MARCA IS NOT NULL THEN ' ,MARCA ' || MARCA END) || 
       (CASE WHEN SERIEDEVOLUTIVO IS NOT NULL THEN ' ,SERIE:' || SERIEDEVOLUTIVO END) || 
       (CASE WHEN SERIE NOT IN (0) OR SERIE IS NOT NULL THEN ', PLACA:' || SERIE END) DESCRIPCIONDETALLE
FROM  V_MOVIMIENTO_INFORME
WHERE V_MOVIMIENTO_INFORME.COMPANIA       = s$compania$s  
	AND V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO = 's$tipoMovimiento$s'
	AND V_MOVIMIENTO_INFORME.MOVIMIENTO BETWEEN s$movimientoInicial$s AND s$movimientoFinal$s
ORDER BY V_MOVIMIENTO_INFORME.CODIGO,
         V_MOVIMIENTO_INFORME.ELEMENTO
