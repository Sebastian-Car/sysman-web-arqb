SELECT 
  MOVIMIENTO.TIPOMOVIMIENTO,
  MOVIMIENTO.NUMERO,
   TO_CHAR(MOVIMIENTO.FECHA, 'DD/MM/YYYY') AS FECHA,
  TRIM (TO_CHAR(MOVIMIENTO.FECHA, 'MONTH')) || '/' || TO_CHAR(MOVIMIENTO.FECHA, 'YYYY') AS FECHAT,
  SUM(D_MOVIMIENTO.VALORTOTAL) VALORTOTAL, 
  MOVIMIENTO.AJUSTECENTAVOS,
  MOVIMIENTO.TOTALCONAJUSTE,
  MOVIMIENTO.AJUSTECENTAVOS1,
  TIPOMOVIMIENTO.CLASE,
  TIPOMOVIMIENTO.NOMBRE
FROM MOVIMIENTO
INNER JOIN TIPOMOVIMIENTO
   ON MOVIMIENTO.COMPANIA       = TIPOMOVIMIENTO.COMPANIA 
  AND MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
INNER JOIN D_MOVIMIENTO
   ON MOVIMIENTO.COMPANIA        = D_MOVIMIENTO.COMPANIA 
  AND MOVIMIENTO.TIPOMOVIMIENTO  = D_MOVIMIENTO.TIPOMOVIMIENTO
  AND MOVIMIENTO.NUMERO          = D_MOVIMIENTO.MOVIMIENTO
WHERE MOVIMIENTO.COMPANIA = s$compania$s
  AND D_MOVIMIENTO.IND_REG IN (0)
GROUP BY 
  MOVIMIENTO.TIPOMOVIMIENTO,
  MOVIMIENTO.NUMERO,
   TO_CHAR(MOVIMIENTO.FECHA, 'DD/MM/YYYY'),
  TRIM (TO_CHAR(MOVIMIENTO.FECHA, 'MONTH')) || '/' || TO_CHAR(MOVIMIENTO.FECHA, 'YYYY'),
  MOVIMIENTO.AJUSTECENTAVOS,
  MOVIMIENTO.TOTALCONAJUSTE,
  MOVIMIENTO.AJUSTECENTAVOS1,
  TIPOMOVIMIENTO.CLASE,
  TIPOMOVIMIENTO.NOMBRE  
