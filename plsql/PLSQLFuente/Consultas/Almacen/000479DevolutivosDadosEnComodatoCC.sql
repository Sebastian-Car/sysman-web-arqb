		INVENTARIO.NOMBRELARGO,  
		DEVOLUTIVO.ELEMENTO CODIGOELEMENTODEV,  
		DEVOLUTIVO.SERIE,  
		DEVOLUTIVO.DESCRIPCION DEVOLUTIVODESCRIPCION,  
		DEVOLUTIVO.MARCA,  
		DEVOLUTIVO.SERIEDEVOLUTIVO,  
		DEPENDENCIA.NOMBRE DEPENDENCIANOMBRE,  
		TERCERO.NOMBRE RESPONSABLENOMBRE,  
		TRIM(INVENTARIO.NOMBRELARGO)||' -'||DEVOLUTIVO.DESCRIPCION AS NOMBRELARGOS,  
		DEVOLUTIVO.VALOR AS VALORDEVOLUTIVO,  
		PCK_GENERALES.FC_NOMBREINVENTARIO(SUBSTR(DEVOLUTIVO.ELEMENTO,PCK_SYSMAN_UTL.FC_PAR(DEVOLUTIVO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',PCK_DATOS.FC_MODULOALMACEN,SYSDATE)),DEVOLUTIVO.COMPANIA,NULL) NOMBREINVENTARIO, 
		SUBSTR(DEVOLUTIVO.ELEMENTO,1,PCK_SYSMAN_UTL.FC_PAR(DEVOLUTIVO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',PCK_DATOS.FC_MODULOALMACEN,SYSDATE)) AS AGRUPACION,  
		ESTADODEVOLUTIVO.DESCRIPCION ESTADODEVOLUTIVODESCRIPCION,  
		DEVOLUTIVO.DEPENDENCIA,  
		TIPOMOVIMIENTO.CONCEPTO,  
		TIPOMOVIMIENTO.CLASE,  
		INVENTARIO.TIPO 
FROM DEVOLUTIVO  
	LEFT JOIN INVENTARIO  
		ON  DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA  
		AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO 
  LEFT JOIN TIPOMOVIMIENTO  
		ON  DEVOLUTIVO.COMPANIA         = TIPOMOVIMIENTO.COMPANIA  
		AND DEVOLUTIVO.TIPOMOVIMIENTOF  = TIPOMOVIMIENTO.CODIGO 
  LEFT JOIN ESTADODEVOLUTIVO  
		ON DEVOLUTIVO.ESTADO = ESTADODEVOLUTIVO.ESTADO  
	LEFT JOIN DEPENDENCIA  
		ON  DEVOLUTIVO.COMPANIA    = DEPENDENCIA.COMPANIA  
		AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO  
	LEFT JOIN RESPONSABLE  
		ON  DEVOLUTIVO.COMPANIA    = RESPONSABLE.COMPANIA  
		AND DEVOLUTIVO.RESPONSABLE = RESPONSABLE.CEDULA 
		AND DEVOLUTIVO.SUCURSAL_RESPONSABLE = RESPONSABLE.SUCURSAL 
	     INNER JOIN TERCERO  
	      ON  RESPONSABLE.COMPANIA    = TERCERO.COMPANIA  
	      AND RESPONSABLE.CEDULA      = TERCERO.NIT 
	      AND RESPONSABLE.SUCURSAL    = TERCERO.SUCURSAL 
WHERE DEVOLUTIVO.COMPANIA = s$compania$s  
  AND DEVOLUTIVO.ELEMENTO BETWEEN s$elementodesde$s  
                              AND s$elementoHasta$s  
  AND TIPOMOVIMIENTO.CONCEPTO = 'CM'  
  AND TIPOMOVIMIENTO.CLASE    = 'S'  
  AND INVENTARIO.TIPO 	      = 'D' 
ORDER BY INVENTARIO.NOMBRELARGO]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);