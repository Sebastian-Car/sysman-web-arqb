SELECT DISTINCT INVENTARIO.COMPANIA,
  INVENTARIO.CODIGOELEMENTO,
  INVENTARIO.NOMBRELARGO
FROM INVENTARIO
	LEFT JOIN ALMACENCONTABILIDAD
		 ON INVENTARIO.COMPANIA        = ALMACENCONTABILIDAD.COMPANIA
		AND INVENTARIO.CODIGOELEMENTO  = ALMACENCONTABILIDAD.CODIGOELEMENTO
    AND ALMACENCONTABILIDAD.ANO        = s$anio$s
	LEFT JOIN INVENTARIOCONTABILIDAD
		 ON INVENTARIO.COMPANIA        = INVENTARIOCONTABILIDAD.COMPANIA
		AND INVENTARIO.CODIGOELEMENTO  = INVENTARIOCONTABILIDAD.CODIGOELEMENTO
    AND INVENTARIOCONTABILIDAD.ANO 	   = s$anio$s
WHERE INVENTARIO.COMPANIA     			   = s$compania$s
	AND INVENTARIO.CODIGOELEMENTO          BETWEEN 's$elementoDesde$s' AND 's$elementoHasta$s'
	AND (ALMACENCONTABILIDAD.CUENTADEBITO  IS NOT NULL
	  OR ALMACENCONTABILIDAD.CUENTACREDITO IS NOT NULL)
ORDER BY INVENTARIO.CODIGOELEMENTO
