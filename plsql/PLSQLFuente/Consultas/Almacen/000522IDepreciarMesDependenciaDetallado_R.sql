CASE
    WHEN PCK_SYSMAN_UTL.FC_PAR(DEPRECIAR.COMPANIA,'MANEJA NIIF EN ALMACEN',10,DEPRECIAR.PERIODO)='SI' THEN NIIF_INVENTARIOCONTA.CUENTACREDITO
    ELSE INVENTARIOCONTABILIDAD.DEPRECCREDITOS
END CODIGOCUENTA,
CASE
    WHEN PCK_SYSMAN_UTL.FC_PAR(DEPRECIAR.COMPANIA,'MANEJA NIIF EN ALMACEN',10,DEPRECIAR.PERIODO)='SI' THEN PLAN_CONTABLE.NOMBRE
    ELSE PLANCONTABLE.NOMBRE
END NOMBRECUENTA,
CASE 
         WHEN PCK_SYSMAN_UTL.FC_PAR(DEPRECIAR.COMPANIA, 'APLICAR DETERIORO ANTES DE DEPRECIACION', 10, SYSDATE) = 'NO' THEN
            (SELECT NVL(SUM(RP2.DETERIORO), 0)
             FROM REEXPRESARVIDAUTIL RP2
             WHERE RP2.COMPANIA = DEVOLUTIVO.COMPANIA
               AND RP2.ELEMENTO = DEVOLUTIVO.ELEMENTO
               AND RP2.SERIE = DEVOLUTIVO.SERIE
               AND TO_CHAR(TRUNC(RP2.FECHACAMBIO, 'MM'), 'YYYYMM') = 
                   TO_CHAR(ADD_MONTHS(DEPRECIAR.PERIODO, -1), 'YYYYMM'))

