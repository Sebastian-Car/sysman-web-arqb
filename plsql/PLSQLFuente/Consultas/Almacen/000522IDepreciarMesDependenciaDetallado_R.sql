            (SELECT NVL(SUM(RP2.DETERIORO), 0)
             FROM REEXPRESARVIDAUTIL RP2
             WHERE RP2.COMPANIA = DEVOLUTIVO.COMPANIA
               AND RP2.ELEMENTO = DEVOLUTIVO.ELEMENTO
               AND RP2.SERIE = DEVOLUTIVO.SERIE
               AND TO_CHAR(TRUNC(RP2.FECHACAMBIO, 'MM'), 'YYYYMM') = 
