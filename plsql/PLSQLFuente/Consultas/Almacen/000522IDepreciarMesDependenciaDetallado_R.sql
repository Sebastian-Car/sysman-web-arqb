CASE
    WHEN PCK_SYSMAN_UTL.FC_PAR(DEPRECIAR.COMPANIA,'MANEJA NIIF EN ALMACEN',10,DEPRECIAR.PERIODO)='SI' THEN DEPRECIAR.NIIF_VLRDEPRECIACION
    ELSE DEPRECIAR.VLRDEPRECIACION
