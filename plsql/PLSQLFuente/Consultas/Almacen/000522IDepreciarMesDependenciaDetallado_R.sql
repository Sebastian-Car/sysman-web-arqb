AND INVENTARIOCONTABILIDAD.DEPRECCREDITO = PLANCONTABLE.CODIGO
AND INVENTARIOCONTABILIDAD.ANO = PLANCONTABLE.ANO
WHERE DEPRECIAR.COMPANIA=s$compania$s
  AND DEPRECIAR.ELEMENTO BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s' 
  AND TO_CHAR(DEPRECIAR.PERIODO,'DD/MM/YYYY')='s$ultimoDia$s'
  AND TO_DATE(TO_CHAR(NVL(FECHAANULADA,TO_DATE('31/12/3000')), 'DD/MM/YYYY'), 'DD/MM/YYYY')>TO_DATE('s$ultimoDia$s', 'DD/MM/YYYY')
  AND BODEGA.CLASE_BODEGA IN (50)
  AND TO_DATE(TO_CHAR(DEVOLUTIVO.FECHAADQUISICION, 'DD/MM/YYYY'), 'DD/MM/YYYY')<=TO_DATE('s$ultimoDia$s', 'DD/MM/YYYY')
  s$filtro$s
ORDER BY DEPR]') || TO_CLOB(q'[ECIAR.COMPANIA, 
DEPRECIAR.ELEMENTO, 
DEPRECIAR.SERIE, 
DEPRECIAR.PERIODO]') AS CONSULTA_VAL, 10 AS APLICACION_VAL, TO_CLOB(q'[]') AS CONSULTA_OPCIONAL_VAL, (SELECT MIN(ROWID) FROM CONSULTAS WHERE INFORME = '000522IDepreciarMesDependenciaDetallado_R') AS RID_DESTINO  FROM DUAL ) INI ON (FIN.ROWID = INI.RID_DESTINO)  WHEN MATCHED THEN   UPDATE SET FIN.CONSULTA = INI.CONSULTA_VAL, FIN.APLICACION = INI.APLICACION_VAL, FIN.CONSULTA_OPCIONAL = INI.CONSULTA_OPCIONAL_VAL, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN   INSERT (INFORME, CONSULTA, APLICACION, CONSULTA_OPCIONAL, DATE_CREATED)   VALUES (INI.INFORME_VAL, INI.CONSULTA_VAL, INI.APLICACION_VAL, INI.CONSULTA_OPCIONAL_VAL, SYSDATE);