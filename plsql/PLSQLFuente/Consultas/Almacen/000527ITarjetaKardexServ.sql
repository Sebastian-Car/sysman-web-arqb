WITH C_HOJAKARDEXDEVSERV AS (
      SELECT  TIPOMOVIMIENTO.COMPANIA, 
              TIPOMOVIMIENTO.CODIGO, 
              TIPOMOVIMIENTO.CONCEPTO, 
              TIPOMOVIMIENTO.CLASE, 
              TIPOMOVIMIENTO.TIPOELEMENTO, 
              DEPENDENCIA.NOMBRE DEPENDENCIA,
              TERCERO.NOMBRE RESPONSABLE,
              D_MOVIMIENTO.ELEMENTO AS CODIGOELEMENTO,
              D_MOVIMIENTO.ESTADO,
              TIPOMOVIMIENTO.NOMBRE, 
              D_MOVIMIENTO.VALORTOTAL AS VLRTOTAL, 
              D_MOVIMIENTO.SERIE AS SERIE, 
              D_MOVIMIENTO.MOVIMIENTO AS MOVIMIENTO,
              CASE WHEN V_MOVIMIENTO.CLASE = 'E' THEN 'S' ELSE 'E' END  AS CLASE1,
              CASE WHEN V_MOVIMIENTO.CLASE ='T' THEN 'E' ELSE 'S' END AS CLASE2,
              D_MOVIMIENTO.FECHA,
              TO_CHAR(D_MOVIMIENTO.HORA,'HH:MI:SS') AS HORA ,
              CASE WHEN CASE WHEN V_MOVIMIENTO.CLASE ='T' 
                THEN 'E' ELSE 'S' END IS NULL THEN CASE WHEN V_MOVIMIENTO.CLASE = 'E' THEN 'S' ELSE 'E' END  
                ELSE CASE WHEN V_MOVIMIENTO.CLASE ='T' THEN 'E' ELSE 'S' END
              END AS CLASE3,
              LPAD(D_MOVIMIENTO.ELEMENTO,3)AS AGRUPACION,
              D_MOVIMIENTO.VALORUNITARIO AS VALORUNITARIO1,
              CASE WHEN 
                CASE WHEN CASE WHEN V_MOVIMIENTO.CLASE ='T' 
                  THEN 'E' ELSE 'S' END IS NULL THEN CASE WHEN V_MOVIMIENTO.CLASE = 'E' THEN 'S' ELSE 'E' END  
                ELSE CASE WHEN V_MOVIMIENTO.CLASE ='T' THEN 'E' ELSE 'S' END
              END = 'E'
              THEN 
                CASE WHEN D_MOVIMIENTO.CANTIDAD IS NOT NULL THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END 
              ELSE 
                0
              END AS CANTIDADENTRADAS,
              CASE WHEN 
                CASE WHEN CASE WHEN V_MOVIMIENTO.CLASE ='T' 
                  THEN 'E' ELSE 'S' END IS NULL THEN CASE WHEN V_MOVIMIENTO.CLASE = 'E' THEN 'S' ELSE 'E' END  
                  ELSE CASE WHEN V_MOVIMIENTO.CLASE ='T' THEN 'E' ELSE 'S' END
                END = 'S'
              THEN 
                CASE WHEN D_MOVIMIENTO.CANTIDAD IS NOT NULL THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END 
              ELSE 
                0
              END  AS CANTIDADSALIDAS,
              CASE WHEN 
                CASE WHEN CASE WHEN V_MOVIMIENTO.CLASE ='T' 
                  THEN 'E' ELSE 'S' END IS NULL THEN CASE WHEN V_MOVIMIENTO.CLASE = 'E' THEN 'S' ELSE 'E' END  
                ELSE CASE WHEN V_MOVIMIENTO.CLASE ='T' THEN 'E' ELSE 'S' END
                END = 'E'
              THEN 
                CASE WHEN D_MOVIMIENTO.VALORTOTAL IS NOT NULL THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END 
              ELSE 
                0
              END AS VALORENTRADAS,
              CASE WHEN 
                CASE WHEN CASE WHEN V_MOVIMIENTO.CLASE ='T' 
                  THEN 'E' ELSE 'S' END IS NULL THEN CASE WHEN V_MOVIMIENTO.CLASE = 'E' THEN 'S' ELSE 'E' END  
                ELSE CASE WHEN V_MOVIMIENTO.CLASE ='T' THEN 'E' ELSE 'S' END
                END = 'S'
              THEN 
                CASE WHEN D_MOVIMIENTO.VALORTOTAL IS NOT NULL THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END 
              ELSE 
                0
              END   AS VALORSALIDAS,
              CASE WHEN 
                CASE WHEN 
                  CASE WHEN CASE WHEN V_MOVIMIENTO.CLASE ='T' 
                    THEN 'E' 
                    ELSE 'S' 
                    END IS NULL 
                  THEN CASE WHEN V_MOVIMIENTO.CLASE = 'E' THEN 'S' ELSE 'E' END  
                  ELSE CASE WHEN V_MOVIMIENTO.CLASE ='T' THEN 'E' ELSE 'S' END
                  END = 'E'
                THEN 
                  CASE WHEN D_MOVIMIENTO.CANTIDAD IS NOT NULL THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END 
                ELSE 
                  0
                END = 0
              THEN 0
              ELSE 1
              END AS SALDO,
              CASE WHEN 
                CASE WHEN 
                  CASE WHEN 
                    CASE WHEN V_MOVIMIENTO.CLASE ='T' THEN 'E' ELSE 'S' END IS NULL 
                  THEN CASE WHEN V_MOVIMIENTO.CLASE = 'E' THEN 'S' ELSE 'E' END  
                  ELSE CASE WHEN V_MOVIMIENTO.CLASE ='T' THEN 'E' ELSE 'S' END
                  END = 'E'
                THEN 
                  CASE WHEN D_MOVIMIENTO.CANTIDAD IS NOT NULL THEN D_MOVIMIENTO.CANTIDAD ELSE 0 END 
                ELSE 
                  0
                END = 0
              THEN 0
              ELSE D_MOVIMIENTO.VALORTOTAL
              END AS  VALORSALDO
      FROM TIPOMOVIMIENTO
        INNER JOIN  D_MOVIMIENTO  
          ON D_MOVIMIENTO.COMPANIA = TIPOMOVIMIENTO.COMPANIA 
          AND D_MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO 
        INNER JOIN V_MOVIMIENTO 
          ON D_MOVIMIENTO.COMPANIA = V_MOVIMIENTO.COMPANIA 
          AND D_MOVIMIENTO.TIPOMOVIMIENTO = V_MOVIMIENTO.TIPOMOVIMIENTO 
          AND D_MOVIMIENTO.MOVIMIENTO = V_MOVIMIENTO.NUMERO 
        LEFT JOIN DEPENDENCIA 
          ON V_MOVIMIENTO.COMPANIA = DEPENDENCIA.COMPANIA 
          AND V_MOVIMIENTO.DEPENDENCIA_DESTINO = DEPENDENCIA.CODIGO 
        LEFT JOIN TERCERO 
          ON V_MOVIMIENTO.COMPANIA = TERCERO.COMPANIA 
          AND V_MOVIMIENTO.TERCERO = TERCERO.NIT
          AND V_MOVIMIENTO.SUCURSAL = TERCERO.SUCURSAL
        WHERE TIPOMOVIMIENTO.CONCEPTO  IN ('DS','LS','T','L') 
          AND TIPOMOVIMIENTO.CLASE ='T' 
          AND TIPOMOVIMIENTO.TIPOELEMENTO ='D' 
          AND D_MOVIMIENTO.MOVIMIENTO IS NOT NULL OR TIPOMOVIMIENTO.CONCEPTO IN ('D','L','LS','N','R','CM') 
          AND TIPOMOVIMIENTO.CLASE IN ('S','E') 
          AND TIPOMOVIMIENTO.INVENTARIOINICIAL IN(0)
          ),
          TOTALES AS (SELECT   LPAD(ELEMENTO,3) AS AGRUPACIONTOTALES,
                        SUM(CANTIDAD) AS CANTIDAD1, 
                        SUM(VALORUNITARIO) AS VALORUNITARIO, 
                        SUM(VALORTOTAL) AS VALORTOTAL1 
                FROM ELEMENTOBODEGA 
                WHERE COMPANIA = s$compania$s  
                  AND LPAD(ELEMENTO,3) BETWEEN LPAD(s$elementoInicial$s,3) AND LPAD(s$elementoFinal$s,3)
                  AND FECHAENTRADA <= s$fechaFinal$s  
                  AND FECHACANCELACION <= s$fechaFinal$s
                  GROUP BY LPAD(ELEMENTO,3)
)
SELECT  DEVOLUTIVO.COMPANIA, 
        DEVOLUTIVO.ELEMENTO, 
        C_HOJAKARDEXDEVSERV.FECHA, 
        CASE WHEN NVL(C_HOJAKARDEXDEVSERV.VALORUNITARIO1,0) <>0 THEN 1 ELSE 0 END  AS SALDOANT, 
        NVL(C_HOJAKARDEXDEVSERV.VALORUNITARIO1,0) AS VLRTOTALANT, 
        NVL(C_HOJAKARDEXDEVSERV.VALORUNITARIO1,0) AS VLRUNITANT, 
        INVENTARIO.NOMBRELARGO, 
        DEVOLUTIVO.DESCRIPCION, 
        LPAD(DEVOLUTIVO.ELEMENTO,3) || INVENTARIO.NOMBRELARGO || PCK_SYSMAN_UTL.FC_STRZERO(DEVOLUTIVO.SERIE,10) AS GRUPO,  
        C_HOJAKARDEXDEVSERV.CODIGO, 
        C_HOJAKARDEXDEVSERV.CONCEPTO, 
        C_HOJAKARDEXDEVSERV.CLASE, 
        C_HOJAKARDEXDEVSERV.TIPOELEMENTO, 
        C_HOJAKARDEXDEVSERV.DEPENDENCIA, 
        C_HOJAKARDEXDEVSERV.RESPONSABLE, 
        C_HOJAKARDEXDEVSERV.CODIGOELEMENTO, 
        C_HOJAKARDEXDEVSERV.ESTADO, 
        C_HOJAKARDEXDEVSERV.NOMBRE, 
        C_HOJAKARDEXDEVSERV.VLRTOTAL, 
        C_HOJAKARDEXDEVSERV.MOVIMIENTO, 
        C_HOJAKARDEXDEVSERV.CLASE1, 
        C_HOJAKARDEXDEVSERV.CLASE2, 
        C_HOJAKARDEXDEVSERV.HORA, 
        C_HOJAKARDEXDEVSERV.CLASE3, 
        C_HOJAKARDEXDEVSERV.VALORUNITARIO1, 
        C_HOJAKARDEXDEVSERV.CANTIDADENTRADAS, 
        C_HOJAKARDEXDEVSERV.CANTIDADSALIDAS, 
        C_HOJAKARDEXDEVSERV.VALORENTRADAS, 
        C_HOJAKARDEXDEVSERV.VALORSALIDAS, 
        C_HOJAKARDEXDEVSERV.SALDO, 
        C_HOJAKARDEXDEVSERV.VALORSALDO, 
        DEVOLUTIVO.SERIE, 
        CASE WHEN TRIM(DEVOLUTIVO.SERIEDEVOLUTIVO) = '' THEN NULL ELSE TRIM(DEVOLUTIVO.SERIEDEVOLUTIVO) END  AS SERIEDEVOLUTIVO1, 
        CASE WHEN TRIM(DEVOLUTIVO.MARCA)='' THEN NULL ELSE TRIM(DEVOLUTIVO.MARCA) END  AS MARCA, 
        INVENTARIO.UNIDAD, 
        LPAD(DEVOLUTIVO.ELEMENTO,3) AS AGRUPACION,
        NVL(TOTALES.CANTIDAD1,0) AS CANTIDAD1,
        NVL(TOTALES.VALORTOTAL1,0) AS VALORTOTAL1,
        PCK_GENERALES.FC_NOMBREINVENTARIO(LPAD(INVENTARIO.CODIGOELEMENTO,
          CASE WHEN 
              s$parametro$s = '' 
              THEN '3' 
              ELSE s$parametro$s
              END) , s$compania$s , NULL) AS NOMBREINVENTARIO,
        INVENTARIO.NOMBRELARGO || '' || 'Unidad:' || INVENTARIO.UNIDAD AS NOMBRELAR,
        CASE WHEN INVENTARIO.MARCA IS NOT NULL  OR INVENTARIO.MARCA = ' '
          THEN 'Marca ' || INVENTARIO.MARCA || ' ' 
          ELSE '' 
        END || CASE WHEN INVENTARIO.DESCRIPCION IS NULL THEN  ' ' ELSE INVENTARIO.DESCRIPCION END ||
        CASE WHEN DEVOLUTIVO.SERIEDEVOLUTIVO IS NULL THEN '' 
           ELSE 'Serie:' ||DEVOLUTIVO.SERIEDEVOLUTIVO  || CASE WHEN INVENTARIO.SERIE =0 THEN '' ELSE '' END
        END AS DETALLE  
  FROM DEVOLUTIVO 
  LEFT JOIN C_HOJAKARDEXDEVSERV 
    ON DEVOLUTIVO.COMPANIA  = C_HOJAKARDEXDEVSERV.COMPANIA 
    AND DEVOLUTIVO.ELEMENTO = C_HOJAKARDEXDEVSERV.CODIGOELEMENTO 
    AND DEVOLUTIVO.SERIE    = C_HOJAKARDEXDEVSERV.SERIE 
  LEFT JOIN INVENTARIO  
   ON DEVOLUTIVO.COMPANIA   = INVENTARIO.COMPANIA 
   AND DEVOLUTIVO.ELEMENTO  = INVENTARIO.CODIGOELEMENTO
  LEFT JOIN TOTALES ON  LPAD(DEVOLUTIVO.ELEMENTO,3) = TOTALES.AGRUPACIONTOTALES
WHERE DEVOLUTIVO.COMPANIA = s$compania$s
  AND DEVOLUTIVO.ELEMENTO BETWEEN s$elementoInicial$s AND s$elementoFinal$s 
  AND C_HOJAKARDEXDEVSERV.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND C_HOJAKARDEXDEVSERV.SERIE IS NOT NULL
ORDER BY DEVOLUTIVO.COMPANIA, DEVOLUTIVO.ELEMENTO, DEVOLUTIVO.SERIE
