SELECT 
  COMPANIA,
  TIPOMOVIMIENTO,
  ELEMENTO CODIGO,
  DESCRIPCION_INV NOMBRELARGO,
    CASE WHEN IND_REG IS NULL OR  IND_REG IN (0) THEN
  '  '
  ELSE
  'X' END NOREGISTRADO,
  TIPOMOVIMIENTO_NOM TIPOMOVIMIENTO_NOMBRE,
  TIPOMOVIMIENTO_NOMCORTO NOMBRECORTO,
  UNIDAD,
  CANTIDAD,
  VALORUNITARIO,
  VALORTOTAL_D VALORTOTAL,
  FECHA_MOV  FECHA,
  MOVASOCIADO,
TO_CHAR(FECHAMOVASOCIADO,'DD/MM/YYYY') FECHAMOVASOCIADO,
  DESCRIPCION_MOV DESCRIPCION,
  DEPENDENCIA_DESTINO_NOM DEPENDENCIA_NOMBRE, 
  RESPONSABLE_DESTINO_NOM TERCERO_NOMBRE, 
  TIPOMOVASOCIADO_NOM DOCASOCIADO_NOMBRE,
  RESPONSABLE_DESTINO CEDULA,
  NVL(VALORDOCASOCIADO,0) VALORDOCASOCIADO,
  AJUSTECENTAVOS1_MOV AJUSTECENTAVOS1,
  TOTALCONAJUSTE_MOV TOTALCONAJUSTE,
  (TIPOMOVIMIENTO || MOVIMIENTO)  GRUPO,
  TIPOPESONA,
  AJUSTECENTAVOS_D DETALLEAJUSTE,
  TERCERO NIT, 
  MOVIMIENTO
FROM V_MOVIMIENTO_INFORME 
WHERE   COMPANIA       = s$compania$s   
  AND   TIPOMOVIMIENTO = 's$tipoMovimiento$s'  
  AND   MOVIMIENTO  BETWEEN s$movimientoInicial$s AND s$movimientoFinal$s  
ORDER BY   MOVIMIENTO
