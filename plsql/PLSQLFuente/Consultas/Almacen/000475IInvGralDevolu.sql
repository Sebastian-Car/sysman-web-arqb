SELECT DEVOLUTIVO.COMPANIA, 
       DEVOLUTIVO.ELEMENTO,
       DEVOLUTIVO.SERIE, 
       V_MOVIMIENTOHASTAUNAFECHA.DEPENDENCIA_DESTINO  DEPENDENCIA,
       DEPENDENCIA.NOMBRE NOMBREDEPENDENCIA, 
       LPAD(V_MOVIMIENTOHASTAUNAFECHA.ELEMENTO, 's$digitosAgrupacion$s') AGRUPACION,
       TRIM(INVENTARIO.NOMBRELARGO) || ' ' || DEVOLUTIVO.DESCRIPCION || 
       CASE WHEN(NVL(DEVOLUTIVO.MARCA,' ')=' ')         THEN '' ELSE 'MARCA: '|| DEVOLUTIVO.MARCA END  ||
       CASE WHEN TRIM(DEVOLUTIVO.SERIEDEVOLUTIVO) = ''  THEN '' ELSE 'SERIE:' || DEVOLUTIVO.SERIEDEVOLUTIVO END  NOMBRELARGO,
       CASE WHEN V_MOVIMIENTOHASTAUNAFECHA.TIPOMOVIMIENTO = 'CDV' THEN DEVOLUTIVO.VALOR ELSE V_MOVIMIENTOHASTAUNAFECHA.VALORTOTAL END  VALORTOTAL,
       V_MOVIMIENTOHASTAUNAFECHA.TIPOMOVIMIENTO ULTIMODETIPOMOVIMIENTO,
       V_MOVIMIENTOHASTAUNAFECHA.MOVIMIENTO ULTIMODEMOVIMIENTO,
       ESTADODEVOLUTIVO.DESCRIPCION ESTADO,
       TO_CHAR(V_MOVIMIENTOHASTAUNAFECHA.FECHA,'DD/MM/YYYY') ULTIMODEFECHA,      
       DEVOLUTIVO.RESPONSABLE,
       TERCERO.NOMBRE NOMBRERESPONSABLE       
FROM DEVOLUTIVO 
LEFT JOIN INVENTARIO
  ON DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA
 AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
INNER JOIN DEPENDENCIA
  ON DEVOLUTIVO.COMPANIA = DEPENDENCIA.COMPANIA
 AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO 
INNER JOIN TERCERO
  ON TERCERO.COMPANIA    = DEVOLUTIVO.COMPANIA
 AND TERCERO.SUCURSAL    = DEVOLUTIVO.SUCURSAL_RESPONSABLE
 AND TERCERO.NIT         = DEVOLUTIVO.RESPONSABLE
INNER JOIN V_MOVIMIENTOHASTAUNAFECHA
  ON DEVOLUTIVO.COMPANIA  = V_MOVIMIENTOHASTAUNAFECHA.COMPANIA
 AND DEVOLUTIVO.ELEMENTO  = V_MOVIMIENTOHASTAUNAFECHA.ELEMENTO
 AND DEVOLUTIVO.SERIE     = V_MOVIMIENTOHASTAUNAFECHA.SERIE
INNER JOIN ESTADODEVOLUTIVO 
  ON DEVOLUTIVO.ESTADO  = ESTADODEVOLUTIVO.ESTADO  
WHERE DEVOLUTIVO.COMPANIA      = s$compania$s
  AND DEVOLUTIVO.ELEMENTO BETWEEN 's$cmbElementoDesde$s' AND 's$cmbElementoHasta$s'
  AND DEVOLUTIVO.PLACAANULADA IN(0)
  AND V_MOVIMIENTOHASTAUNAFECHA.CLASE IN('E')