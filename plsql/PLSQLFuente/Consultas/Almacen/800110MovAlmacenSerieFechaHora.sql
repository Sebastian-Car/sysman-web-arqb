    INNER JOIN D_MOVIMIENTO 
         ON D_MOVIMIENTO.COMPANIA       = MOVIMIENTO.COMPANIA 
        AND D_MOVIMIENTO.MOVIMIENTO     = MOVIMIENTO.NUMERO 
        AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO 
    INNER JOIN 
        (
        SELECT  D_MOVIMIENTO.COMPANIA
               ,D_MOVIMIENTO.ELEMENTO
               ,D_MOVIMIENTO.SERIE
               ,MAX(TO_DATE(TO_CHAR(D_MOVIMIENTO.FECHA 
                                    + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA,'hh24'),'HOUR'  ) 
                                    + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA,'mi'  ),'MINUTE') 
                                    + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA,'ss'  ),'SECOND')
                            
                                   ,'DD/MM/YYYY hh24:mi:ss')
                            ,'DD/MM/YYYY hh24:mi:ss')
                    ) FECHA_HORA
          FROM D_MOVIMIENTO  
         WHERE IND_REG NOT IN(0)
           AND TO_DATE(TO_CHAR(D_MOVIMIENTO.FECHA 
                               + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA,'hh24'),'HOUR'  ) 
                               + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA,'mi'  ),'MINUTE') 
                               + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA,'ss'  ),'SECOND')
                               ,'DD/MM/YYYY hh24:mi:ss')
                       ,'DD/MM/YYYY hh24:mi:ss')          <= TO_DATE('s$fechaHasta$s', 'DD/MM/YYYY hh24:mi:ss')
          GROUP BY  D_MOVIMIENTO.COMPANIA
                   ,D_MOVIMIENTO.ELEMENTO
                   ,D_MOVIMIENTO.SERIE
        ) ULTIMO
     ON D_MOVIMIENTO.COMPANIA =ULTIMO.COMPANIA
    AND D_MOVIMIENTO.ELEMENTO =ULTIMO.ELEMENTO
    AND D_MOVIMIENTO.SERIE    =ULTIMO.SERIE
    AND TO_DATE(TO_CHAR(D_MOVIMIENTO.FECHA 
                        + NUMTODSINTERVAL(TO_CHAR(D_MOVIMIENTO.HORA,'hh24'),'HOUR'  ) 
