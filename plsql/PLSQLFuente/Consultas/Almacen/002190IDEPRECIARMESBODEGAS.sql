 SELECT 
 INVENTARIOCONTABILIDAD.DEPRECCREDITO CUENTACREDITO,
 INVENTARIOCONTABILIDAD.DEPRECDEBITO CUENTADEBITO,
 DEVOLUTIVO.COMPANIA, 
 DEVOLUTIVO.ELEMENTO,
 PCK_ALMACEN.FC_NOMBREINVENTARIO(DEPRECIAR.COMPANIA,SUBSTR(DEVOLUTIVO.ELEMENTO,0,3),0) NOMBREINVENTARIO,
 INVENTARIO.NOMBRELARGO|| DEVOLUTIVO.DESCRIPCION || CASE WHEN TRIM(DEVOLUTIVO.MARCA) IS NULL THEN '' ELSE ' MARCA: '||DEVOLUTIVO.MARCA END NOMBRELARGOS,
 DEVOLUTIVO.SERIE,
 TO_CHAR(DEPRECIAR.PERIODO,'YYYY') AS ANO,
 TO_CHAR(DEPRECIAR.PERIODO,'MM') AS MES,
 DEPRECIAR.DEPENDENCIA,
 DEVOLUTIVO.DESCRIPCION,
 INVENTARIO.NOMBRELARGO,
 INVENTARIO.CUENTAACTIVO,
 SUBSTR(DEVOLUTIVO.ELEMENTO,0,3) GRUPO,
 DEVOLUTIVO.VLRDEPRECIACIONAJ,
 DEVOLUTIVO.COSTOAJUSTADO, 
 DEPRECIAR.VLRLIBROS, 
 DEPRECIAR.DEPACUMULADAAJ, 
 DEPRECIAR.VLRAJUSTADO, 
 DEPRECIAR.AJDEPACUMULADA,
 DEPRECIAR.VLRELEMENTO,
 DEPRECIAR.DEPACUMULADAAJ
 FROM DEVOLUTIVO 
 LEFT JOIN INVENTARIO 
 ON DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
 AND DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA
 LEFT JOIN DEPRECIAR
 ON DEVOLUTIVO.COMPANIA = DEPRECIAR.COMPANIA
 AND DEVOLUTIVO.ELEMENTO = DEPRECIAR.ELEMENTO
 AND DEVOLUTIVO.SERIE = DEPRECIAR.SERIE
 LEFT JOIN INVENTARIOCONTABILIDAD
 ON DEVOLUTIVO.ELEMENTO = INVENTARIOCONTABILIDAD.CODIGOELEMENTO
 AND DEVOLUTIVO.COMPANIA = INVENTARIOCONTABILIDAD.COMPANIA
 WHERE
 DEVOLUTIVO.COMPANIA = s$compania$s
 AND DEVOLUTIVO.ELEMENTO BETWEEN s$elementoInicial$s AND s$elementoFinal$s
 AND DEVOLUTIVO.SERIE  BETWEEN s$placaInicial$s AND s$placaFinal$s
 AND TO_CHAR(DEPRECIAR.PERIODO,'YYYY')= s$ano$s 
 AND TO_CHAR(DEPRECIAR.PERIODO,'MM') = s$mes$s
 AND TO_CHAR(NVL(FECHAANULADA,'31/12/3000'), 'DD/MM/YYYY')>TO_DATE('s$ultimoDia$s', 'DD/MM/YYYY')
s$dependencia$s
 ORDER BY 
 DEVOLUTIVO.COMPANIA, 
 DEVOLUTIVO.ELEMENTO, 
 DEVOLUTIVO.SERIE