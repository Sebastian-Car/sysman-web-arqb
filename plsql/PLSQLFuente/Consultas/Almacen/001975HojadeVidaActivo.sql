MERGE INTO CONSULTAS FIN USING (SELECT '001975HojadeVidaActivo' INFORME ,TO_CLOB(q'[SELECT 
	DEVOLUTIVO.SERIEDEVOLUTIVO SERIAL,
	DEVOLUTIVO.SERIE,
	DEVOLUTIVO.VALOR,
	DEVOLUTIVO.MARCA,
	TO_CHAR(DEVOLUTIVO.FECHAADQUISICION,'DD/MM/YYYY') FECHAADQUISICION,
	DEVOLUTIVO.DESCRIPCION,
 	DEVOLUTIVO.MODELO,
	s$mesesvidautil$s 
	CASE WHEN IMAGEN_ELEMENTOS.RUTA IS NOT NULL
	     THEN (SELECT RUTA_ARCHIVOS || 'Imagenes' || s$separador$s || 'CatalogoElementos' || s$separador$s
	            FROM APLICACIONES
	            WHERE APLICACION = 10)|| IMAGEN_ELEMENTOS.RUTA
	     ELSE ''
	END RUTA,
	DEPENDENCIA.NOMBRE UBICACION,
	INVENTARIO.CODIGOELEMENTO,
	INVENTARIO.NOMBRELARGO,
	RESPONSABLE.CEDULA,
	TERCERO.NOMBRE,
	RESPONSABLE.SUCURSAL,
    DEPRECIAR.NIIF_VLRACUMULADO,
    DEPRECIAR.NIIF_VLRLIBROS
FROM DEVOLUTIVO
INNER JOIN DEPENDENCIA
ON DEVOLUTIVO.COMPANIA 		= DEPENDENCIA.COMPANIA
AND DEVOLUTIVO.DEPENDENCIA  = DEPENDENCIA.CODIGO
INNER JOIN INVENTARIO 
ON DEVOLUTIVO.COMPANIA 	= INVENTARIO.COMPANIA
AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
LEFT JOIN IMAGEN_ELEMENTOS
ON IMAGEN_ELEMENTOS.COMPANIA = INVENTARIO.COMPANIA
AND IMAGEN_ELEMENTOS.ELEMENTO = INVENTARIO.CODIGOELEMENTO
LEFT JOIN ACTDEPRECIABLE 
ON ACTDEPRECIABLE.CODIGO = INVENTARIO.TIPOACTIVO
INNER JOIN RESPONSABLE
ON DEVOLUTIVO.COMPANIA 		= RESPONSABLE.COMPANIA
AND DEVOLUTIVO.RESPONSABLE 	= RESPONSABLE.CEDULA
AND DEVOLUTIVO.SUCURSAL_RESPONSABLE = RESPONSABLE.SUCURSAL
INNER JOIN TERCERO
ON RESPONSABLE.COMPANIA = TERCERO.COMPANIA
AND RESPONSABLE.CEDULA = TERCERO.NIT
AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
LEFT JOIN (SELECT COMPANIA, ELEMENTO, SERIE,
            ANO, MES, NIIF_VLRACUMULADO, NIIF_VLRLIBROS
FROM (SELECT D.*, ROW_NUMBER() OVER(
      PARTITION BY COMPANIA, ELEMENTO, SERIE
      ORDER BY ANO DESC, MES DESC ) AS RN
    FROM DEPRECIAR D
    WHERE COMPANIA = s$compania$s) WHERE RN = 1) DEPRECIAR
ON DEVOLUTIVO.COMPANIA = DEPRECIAR.COMPANIA
AND DEVOLUTIVO.ELEMENTO = DEPRECIAR.ELEMENTO
AND DEVOLUTIVO.SERIE = DEPRECIAR.SERIE
WHERE DEVOLUTIVO.COMPANIA = s$compania$s
AND DEVOLUTIVO.SERIE BETWEEN 's$serieInicial$s' AND 's$serieFinal$s'
ORDER BY DEVOLUTIVO.SERIE
]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);