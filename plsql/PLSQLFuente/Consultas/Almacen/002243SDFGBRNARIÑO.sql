SELECT 
  V_MOVIMIENTO_INFORME.CANTIDAD,
  V_MOVIMIENTO_INFORME.ELEMENTO,
  V_MOVIMIENTO_INFORME.VALORUNITARIO,
  V_MOVIMIENTO_INFORME.VALORTOTAL_D,
  V_MOVIMIENTO_INFORME.MOVIMIENTO CONSECUTIVO,
  TO_CHAR(V_MOVIMIENTO_INFORME.FECHA_MOV, 'DD/MM/YYYY') FECHA_MOV,
  TO_CHAR(V_MOVIMIENTO_INFORME.FECHA_MOV, 'DD/MM/YYYY') FECHA,
  TO_CHAR(V_MOVIMIENTO_INFORME.FECHA_MOV, 'DD/MM/YYYY') FECHATRASPASO,
  V_MOVIMIENTO_INFORME.DEPENDENCIA_ORIGEN DEPENDENCIAANT,
  V_MOVIMIENTO_INFORME.RESPONSABLE_ORIGEN RESPONSABLEANT,
  V_MOVIMIENTO_INFORME.DEPENDENCIA_DESTINO DEPENDENCIANUE,
  V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO RESPONSABLENUE,
  V_MOVIMIENTO_INFORME.DESCRIPCION_MOV DESCRIPCION, 
  V_MOVIMIENTO_INFORME.DEPENDENCIA_ORIGEN_NOM  ,
  V_MOVIMIENTO_INFORME.RESPONSABLE_ORIGEN_NOM  ,
  V_MOVIMIENTO_INFORME.DEPENDENCIA_DESTINO_NOM  ,
  V_MOVIMIENTO_INFORME.DEPENDENCIA_DESTINO_NOM  ,
  V_MOVIMIENTO_INFORME.RESPONSABLE_ORIGEN_CARGO,
  V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO_CARGO ,
  V_MOVIMIENTO_INFORME.NOMBRELARGO || ' ' || CASE WHEN V_MOVIMIENTO_INFORME.MARCA IS NOT  NULL  THEN ('MARCA: ' || V_MOVIMIENTO_INFORME.MARCA) END || ' ' || V_MOVIMIENTO_INFORME.DESCRIPCION_MOV   NOMBRELARGO,
  V_MOVIMIENTO_INFORME.UNIDAD,
  V_MOVIMIENTO_INFORME.SERIE || V_MOVIMIENTO_INFORME.DESCRIPCION_INV  DESCRIPCION_INV,
  V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO_NOM,
  V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO,
  V_MOVIMIENTO_INFORME.SERIEDEVOLUTIVO,
  V_MOVIMIENTO_INFORME.SERIE,
  V_MOVIMIENTO_INFORME.COMPANIA,
  V_MOVIMIENTO_INFORME.MARCA,
  V_MOVIMIENTO_INFORME.CONCEPTO,
  V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO || V_MOVIMIENTO_INFORME.MOVIMIENTO AS GRUPO,
  V_MOVIMIENTO_INFORME.RESPONSABLE_ORIGEN CEDULAANTERIOR ,		
  V_MOVIMIENTO_INFORME.RESPONSABLE_ORIGEN_NOM  RESPONSABLEANTERIOR,
  V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO CEDULANUEVA,
  V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO_NOM  RESPONSABLENUEVO,
  TO_CHAR(V_MOVIMIENTO_INFORME.FECHA_MOV, 'DD/MM/YYYY') FECHATRASPASO,
V_MOVIMIENTO_INFORME.CREATED_BY ELABORADO,
   CASE WHEN V_MOVIMIENTO_INFORME.TIPOELEMENTO IN ('C') 
    THEN V_MOVIMIENTO_INFORME.NOMBRELARGO || '' || V_MOVIMIENTO_INFORME.SERIE || ''||V_MOVIMIENTO_INFORME.ESPECIFICACION 
    ELSE V_MOVIMIENTO_INFORME.NOMBRELARGO || '' || V_MOVIMIENTO_INFORME.SERIE ||  ''|| DEVOLUTIVO.DESCRIPCION 
  END  DESCRIPCION_REPORTE
FROM V_MOVIMIENTO_INFORME 
  LEFT JOIN DEVOLUTIVO
 ON V_MOVIMIENTO_INFORME.COMPANIA = DEVOLUTIVO.COMPANIA
AND V_MOVIMIENTO_INFORME.ELEMENTO = DEVOLUTIVO.ELEMENTO
AND V_MOVIMIENTO_INFORME.SERIE = DEVOLUTIVO.SERIE
WHERE V_MOVIMIENTO_INFORME.COMPANIA= s$compania$s 
       AND V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO= 's$tipoMovimiento$s'
       AND V_MOVIMIENTO_INFORME.MOVIMIENTO= s$movimientoInicial$s