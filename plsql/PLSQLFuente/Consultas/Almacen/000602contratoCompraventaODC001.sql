SELECT  ORDENDECOMPRA.TERCERO,
  ORDENDECOMPRA.OBJETOCONTRATO,
  ORDENDECOMPRA.DESCRIPCION,
   V_ORDENDECOMPRA_DIS.FECHACDP AS FECHADISPONIBILIDAD,
  V_ORDENDECOMPRA_DIS.COMPROBANTES DISPONIBILIDAD,
  V_ORDENDECOMPRA_DIS.FECHACDP AS FECHADEDISPONIBILIDAD,
  ORDENDECOMPRA.DURACION,
  '                   ' || ORDENDECOMPRA.GARANTIA AS GARANTIA, 
  ORDENDECOMPRA.LUGARDEENTREGA, 
  ORDENDECOMPRA.SUBTOTAL,
  ORDENDECOMPRA.INTERVENTOR, 
  ORDENDECOMPRA.NUMCOTIZACION, 
  V_ORDENDECOMPRA_RUBRO.RUBRO, 
  ORDENDECOMPRA.VALORIVA, 
  ORDENDECOMPRA.VALORDESCUENTO, 
  ORDENDECOMPRA.AJUSTEPESO, 
  ORDENDECOMPRA.FORMADEPAGO,
  ORDENDECOMPRA.VALORDISPONIBILIDAD,
  V_ORDENDECOMPRA_DIS.COMPROBANTES DISPONIBILIDAD,
  ORDENDECOMPRA.NOTAS,
  ORDENDECOMPRA.CLASEORDEN, 
  D_ORDENDECOMPRA.ORDENDECOMPRA,
  D_ORDENDECOMPRA.CANTIDAD,
  D_ORDENDECOMPRA.VALORUNITARIO,
  D_ORDENDECOMPRA.VALORUNITARIO * D_ORDENDECOMPRA.CANTIDAD AS VALORTOTAL1,
  EXTRACT(YEAR FROM FECHA) AS VIGENCIA,
  ORDENDECOMPRA.VALORTOTAL AS VALTOTAL,
  D_ORDENDECOMPRA.ELEMENTO AS CODIGO, 
  NVL(CIUDAD.NOMBRE,' ') AS NOMBRECIUDAD, 
  NVL(DEPARTAMENTO.NOMBRE,' ') AS NOMBREDEPARTAMENTO,
  INVENTARIO.NOMBRELARGO,
INVENTARIO.NOMBRELARGO || NVL(D_ORDENDECOMPRA.ESPECIFICACION,' ') || (CASE D_ORDENDECOMPRA.MARCA WHEN NULL THEN ' ' ELSE (' Marca ' || D_ORDENDECOMPRA.MARCA) END) AS NOMBREARTICULO,
  INVENTARIO.UNIDAD, 
  DEPENDENCIA.NOMBRE AS NOMBREDEPENDENCIA, 
  COMPANIA.DIRECCION AS DIRECCIONCOMPANIA, 
  COMPANIA.TELEFONO,
  COMPANIA.NOMBRE AS NOMBRECOMPANIA,
  COMPANIA.SIGLACOMPANIA AS SIGLACOMPANIA,
  TERCERO.AUTORETENEDOR AS ESAUTORETENEDOR, 
  TIPOORDENDECOMPRA.NOMBRE AS NOMBRETIPOORDENDECOMPRA, 
  D_ORDENDECOMPRA.VALORUNITARIODI AS VLRUNITARIOELEMENTO, 
  D_ORDENDECOMPRA.VLRTOTAL AS VLRTOTALELEMENTO, 
  ORDENDECOMPRA.FORMADEPAGO AS MIFORMAPAGO,
  TERCERO.NIT,
  TERCERO.SUCURSAL, 
  TERCERO.DIGITOVERIFICACION, 
  TERCERO.NOMBRE AS NOMBRETERCERO,
  TERCERO.NIT_CEDULA, 
  TERCERO.PROPIETARIO, 
  TERCERO.REGIMEN, 
  NVL(TERCERO.DIRECCION,' ') AS DIRECCIONTERCERO,
  NVL(TERCERO.TELEFONOS,' ') AS TELEFONOS,
  TERCERO.TELEFONOS AS TELEFONOTERCERO, 
  TERCERO.CONTACTO, 
  NVL(TERCERO.FAX,' ') AS FAX, 
  ORDENDECOMPRA.PLAZODEENTREGA, 
  COMPANIA.NITCOMPANIA, 
  ORDENDECOMPRA.FECHAFINALIZACION, 
  ORDENDECOMPRA.FECHA AS FECHAINI,
  ORDENDECOMPRA.FECHA, 
  ORDENDECOMPRA.UNIDAD_PLAZO,
  COMPANIA.NITCOMPANIA,
  ORDENDECOMPRA.DOCUMENTOSDELCONTRATO_AUX AS DOCUMENTOSDELCONTRATO,
  D_ORDENDECOMPRA.COMPANIA
FROM DEPARTAMENTO RIGHT JOIN (CIUDAD 
	RIGHT JOIN ((((D_ORDENDECOMPRA 
	LEFT JOIN INVENTARIO ON 
	(D_ORDENDECOMPRA.ELEMENTO = INVENTARIO.CODIGOELEMENTO) AND 
	(D_ORDENDECOMPRA.COMPANIA = INVENTARIO.COMPANIA)) 
	LEFT JOIN COMPANIA ON 
	D_ORDENDECOMPRA.COMPANIA = COMPANIA.CODIGO)
	LEFT JOIN ((ORDENDECOMPRA 
	LEFT JOIN TERCERO 
  ON (ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA)
  AND(ORDENDECOMPRA.TERCERO = TERCERO.NIT)
  AND(ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL) ) 
	LEFT JOIN DEPENDENCIA ON 
	(ORDENDECOMPRA.COMPANIA = DEPENDENCIA.COMPANIA) AND 
	(ORDENDECOMPRA.DEPENDENCIA = DEPENDENCIA.CODIGO)) ON 
	(D_ORDENDECOMPRA.ORDENDECOMPRA = ORDENDECOMPRA.NUMERO) AND 
	(D_ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRA.CLASEORDEN) AND
	(D_ORDENDECOMPRA.COMPANIA = ORDENDECOMPRA.COMPANIA)) 
	LEFT JOIN TIPOORDENDECOMPRA ON
	(D_ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO) AND
	(D_ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA)) ON
	(CIUDAD.CODIGO = TERCERO.CIUDAD) AND 
	(CIUDAD.DEPARTAMENTO = TERCERO.DEPARTAMENTO) AND 
	(CIUDAD.PAIS = TERCERO.PAIS)) ON 
	(DEPARTAMENTO.CODIGO = CIUDAD.DEPARTAMENTO) AND 
	(DEPARTAMENTO.PAIS = CIUDAD.PAIS) 
  LEFT JOIN V_ORDENDECOMPRA_DIS  ON 
  ORDENDECOMPRA.COMPANIA    = V_ORDENDECOMPRA_DIS.COMPANIA AND
  ORDENDECOMPRA.CLASEORDEN   = V_ORDENDECOMPRA_DIS.CLASEORDEN AND
  ORDENDECOMPRA.NUMERO       = V_ORDENDECOMPRA_DIS.NUMERO 
  LEFT JOIN V_ORDENDECOMPRA_RUBRO  ON
  ORDENDECOMPRA.COMPANIA    = V_ORDENDECOMPRA_RUBRO.COMPANIA AND  
  ORDENDECOMPRA.CLASEORDEN   = V_ORDENDECOMPRA_RUBRO.CLASEORDEN AND 
  ORDENDECOMPRA.NUMERO       = V_ORDENDECOMPRA_RUBRO.NUMERO
WHERE  TERCERO.COMPANIA = s$compania$s
  AND nvl(ORDENDECOMPRA.CLASEORDEN,'')= 's$claseOrden$s'
  AND D_ORDENDECOMPRA.ORDENDECOMPRA = s$numero$s
ORDER BY D_ORDENDECOMPRA.COMPANIA,
 D_ORDENDECOMPRA.ORDENDECOMPRA, 
 D_ORDENDECOMPRA.ELEMENTO
