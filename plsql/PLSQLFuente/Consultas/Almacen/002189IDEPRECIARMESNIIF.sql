SELECT  
    DEVOLUTIVO.ELEMENTO,
    DEVOLUTIVO.SERIE,
    PCK_ALMACEN.FC_NOMBREINVENTARIO(DEPRECIAR.COMPANIA,SUBSTR(DEVOLUTIVO.ELEMENTO,0,s$agrupacion$s),0) NOMBREINVENTARIO,
    NOMBRELARGO|| DEVOLUTIVO.DESCRIPCION || CASE WHEN TRIM(DEVOLUTIVO.MARCA) IS NULL THEN '' ELSE ' MARCA: '||DEVOLUTIVO.MARCA END NOMBRELARGOS,
    DEVOLUTIVO.DESCRIPCION,
    DEPRECIAR.DEPENDENCIA, 
    INVENTARIO.NOMBRELARGO,
    TO_CHAR(DEPRECIAR.PERIODO,'YYYY')EXPR1,
    TO_CHAR(DEPRECIAR.PERIODO,'MM')EXPR2,
    DEPRECIAR.NIIF_VLRAJUSTADO,
    DEPRECIAR.NIIF_VLRLIBROS,
    DEPRECIAR.NIIF_DEPACUMULADAAJ,
    DEPRECIAR.NIIF_VLRDEPRECIACIONAJ,
    DEPRECIAR.NIIF_COSTOAJUSTADO,
    DEPRECIAR.NIIF_VLRELEMENTO,
    DEPRECIAR.NIIF_AJDEPACUMULADA,
    SUBSTR(DEVOLUTIVO.ELEMENTO,0,s$agrupacion$s) AS GRUPO,
    NVL(FECHAANULADA,('31/12/3000')) AS EXPR3
FROM DEVOLUTIVO 
    LEFT JOIN INVENTARIO 
    ON DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO 
    AND DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA 
LEFT JOIN DEPRECIAR 
    ON DEVOLUTIVO.COMPANIA = DEPRECIAR.COMPANIA
    AND DEVOLUTIVO.ELEMENTO = DEPRECIAR.ELEMENTO
    AND DEVOLUTIVO.SERIE = DEPRECIAR.SERIE
WHERE DEVOLUTIVO.COMPANIA = 's$compania$s'
    AND DEVOLUTIVO.ELEMENTO BETWEEN s$elementoInicial$s AND s$elementoFinal$s
    AND DEVOLUTIVO.SERIE BETWEEN s$placaInicial$s AND s$placaFinal$s 
    AND TO_CHAR(DEPRECIAR.PERIODO,'YYYY') = s$ano$s 
    AND TO_CHAR(DEPRECIAR.PERIODO,'MM') = s$mes$s 
    AND TO_DATE(TO_CHAR(NVL(FECHAANULADA,TO_DATE('31/12/3000')), 'DD/MM/YYYY'), 'DD/MM/YYYY')>TO_DATE('s$ultimoDia$s', 'DD/MM/YYYY')
ORDER BY  
    DEVOLUTIVO.ELEMENTO, 
    DEVOLUTIVO.SERIE