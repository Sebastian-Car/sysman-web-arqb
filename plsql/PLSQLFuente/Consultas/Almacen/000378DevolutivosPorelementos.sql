SELECT  INVENTARIO.NOMBRELARGO,  
        DEVOLUTIVO.ELEMENTO AS CODIGOELEMENTODEV,  
        DEVOLUTIVO.SERIE,  
        DEVOLUTIVO.DESCRIPCION AS DESCRIPCION,  
        DEVOLUTIVO.MARCA,  
        DEVOLUTIVO.SERIEDEVOLUTIVO,  
        DEPENDENCIA.NOMBRE AS NOMBREDEPENDENCIA,  
        TERCERO.NOMBRE AS TERCERO,  
        TRIM(NOMBRELARGO) || 'Marca:' || DEVOLUTIVO.MARCA || ' -' ||  
        DEVOLUTIVO.DESCRIPCION ||  
        CASE TRIM(SERIEDEVOLUTIVO) WHEN '' THEN '' ELSE 'SERIE:' || SERIEDEVOLUTIVO END  AS NOMBRELARGOS, 
        DEVOLUTIVO.VALOR AS VALORDEVOLUTIVO,  
        ESTADODEVOLUTIVO.DESCRIPCION,  
        DEVOLUTIVO.DEPENDENCIA,  
        DEVOLUTIVO.DESCRIPCION,  
        DEVOLUTIVO.MARCA 
FROM DEVOLUTIVO  
  INNER JOIN INVENTARIO ON DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA 
    AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO   
  INNER JOIN DEPENDENCIA ON DEVOLUTIVO.COMPANIA = DEPENDENCIA.COMPANIA   
    AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO 
  INNER JOIN RESPONSABLE ON DEVOLUTIVO.COMPANIA = RESPONSABLE.COMPANIA  
    AND DEVOLUTIVO.RESPONSABLE = RESPONSABLE.CEDULA  
    AND DEVOLUTIVO.SUCURSAL_RESPONSABLE = RESPONSABLE.SUCURSAL 
  INNER JOIN ESTADODEVOLUTIVO ON DEVOLUTIVO.ESTADO = ESTADODEVOLUTIVO.ESTADO 
  INNER JOIN TERCERO ON RESPONSABLE.COMPANIA = TERCERO.COMPANIA 
    AND RESPONSABLE.CEDULA = TERCERO.NIT 
    AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL 
WHERE DEVOLUTIVO.ELEMENTO BETWEEN s$elementoDesde$s AND s$elementoHasta$s  
  AND DEVOLUTIVO.COMPANIA = s$compania$s 
  AND DEVOLUTIVO.PLACAANULADA = 0 
ORDER BY DEVOLUTIVO.ELEMENTO
