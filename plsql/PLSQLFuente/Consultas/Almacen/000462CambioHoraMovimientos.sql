SELECT 
       D_MOVIMIENTO.TIPOMOVIMIENTO, 
       D_MOVIMIENTO.MOVIMIENTO, 
       D_MOVIMIENTO.CODIGO, 
       D_MOVIMIENTO.ELEMENTO, 
       D_MOVIMIENTO.SERIE, 
       D_MOVIMIENTO.ESPECIFICACION, 
       TO_CHAR(D_MOVIMIENTO.FECHA,'DD/MM/YYYY') FECHA, 
       TO_CHAR(D_MOVIMIENTO.HORA,'HH12:MI AM') HORA
FROM   D_MOVIMIENTO 
WHERE  D_MOVIMIENTO.COMPANIA =  s$compania$s
AND    D_MOVIMIENTO.TIPOMOVIMIENTO = 's$tipoDeMovimiento$s'                       
AND    D_MOVIMIENTO.MOVIMIENTO =  s$numero$s