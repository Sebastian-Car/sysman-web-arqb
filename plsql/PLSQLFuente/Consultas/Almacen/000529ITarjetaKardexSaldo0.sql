SELECT KARDEXCERO.COMPANIA,
       KARDEXCERO.CODIGOELEMENTO,
       KARDEXCERO.NOMBRELARGO,
       NVL(KARDEXCERO.UNIDAD, '') AS UNIDAD,
       C_DMOVIMIENTOKARDEX.SERIE,
       C_DMOVIMIENTOKARDEX.FECHA,
       TO_CHAR(C_DMOVIMIENTOKARDEX.HORA,'HH:MI:SS') AS HORA,
       C_DMOVIMIENTOKARDEX.TIPOMOVIMIENTO,
       C_DMOVIMIENTOKARDEX.MOVIMIENTO,
       C_DMOVIMIENTOKARDEX.ENTRADAS,
       C_DMOVIMIENTOKARDEX.SALIDAS,
       C_DMOVIMIENTOKARDEX.VALENTRADAS AS VLRENTRADAS,
       C_DMOVIMIENTOKARDEX.VALSALIDAS AS VLRSALIDAS,
       C_DMOVIMIENTOKARDEX.DEPENDENCIA AS NOMBRE,
       C_DMOVIMIENTOKARDEX.TERCERO,
       KARDEXCERO.AGRUPACION AS AGRUPACION,
       KARDEXCERO.NOMBREINVENTARIO,
       CASE WHEN C_DMOVIMIENTOKARDEX.SALDOKARDEX     IS NULL THEN C_DMOVIMIENTOKARDEXANT.SALDOKARDEX     ELSE C_DMOVIMIENTOKARDEX.SALDOKARDEX     END SALDOKARDEX,
       CASE WHEN C_DMOVIMIENTOKARDEX.VLRUNITARIOPROM IS NULL THEN C_DMOVIMIENTOKARDEXANT.VLRUNITARIOPROM ELSE C_DMOVIMIENTOKARDEX.VLRUNITARIOPROM END VLRUNITARIOPROM,
       CASE WHEN C_DMOVIMIENTOKARDEX.VALORTOTAL      IS NULL THEN C_DMOVIMIENTOKARDEXANT.VALORTOTAL      ELSE C_DMOVIMIENTOKARDEX.VALORTOTAL      END VALORTOTAL, 
       CASE WHEN C_DMOVIMIENTOKARDEX.VALORUNITARIO   IS NULL THEN C_DMOVIMIENTOKARDEXANT.VALORUNITARIO   ELSE C_DMOVIMIENTOKARDEX.VALORUNITARIO   END VALORUNITARIO, 
       CASE WHEN C_DMOVIMIENTOKARDEX.VALORSALDO      IS NULL THEN C_DMOVIMIENTOKARDEXANT.VALORSALDO      ELSE C_DMOVIMIENTOKARDEX.VALORSALDO      END VALORSALDO,
       CASE WHEN 
            CASE WHEN C_DMOVIMIENTOKARDEX.SALDOKARDEX     IS NULL THEN C_DMOVIMIENTOKARDEXANT.SALDOKARDEX     ELSE C_DMOVIMIENTOKARDEX.SALDOKARDEX     END =0
              THEN 0
              ELSE
              CASE WHEN C_DMOVIMIENTOKARDEX.VALORSALDO      IS NULL THEN C_DMOVIMIENTOKARDEXANT.VALORSALDO      ELSE C_DMOVIMIENTOKARDEX.VALORSALDO      END / 
              CASE WHEN C_DMOVIMIENTOKARDEX.SALDOKARDEX     IS NULL THEN C_DMOVIMIENTOKARDEXANT.SALDOKARDEX     ELSE C_DMOVIMIENTOKARDEX.SALDOKARDEX  END    
        END AS VLRPROMEDIADO  
FROM

(
SELECT V_ULTIMOMOVIMIENTO_ALM.COMPANIA,
       INVENTARIO.CODIGOELEMENTO,  
       V_ULTIMOMOVIMIENTO_ALM.SALDOKARDEX ULTSALDO,
       INVENTARIO.UNIDAD,
       INVENTARIO.TIPO,
       INVENTARIO.NOMBRELARGO,
       GRUPO.CODIGOELEMENTO AGRUPACION,
       GRUPO.NOMBRELARGO NOMBREINVENTARIO       
FROM V_ULTIMOMOVIMIENTO_ALM
INNER JOIN INVENTARIO GRUPO
 ON V_ULTIMOMOVIMIENTO_ALM.COMPANIA         = GRUPO.COMPANIA
AND LPAD(V_ULTIMOMOVIMIENTO_ALM.ELEMENTO,s$parametro$s) = GRUPO.CODIGOELEMENTO
INNER JOIN INVENTARIO 
 ON V_ULTIMOMOVIMIENTO_ALM.COMPANIA        = INVENTARIO.COMPANIA
AND V_ULTIMOMOVIMIENTO_ALM.ELEMENTO        = INVENTARIO.CODIGOELEMENTO
WHERE INVENTARIO.COMPANIA =  s$compania$s
  AND INVENTARIO.CODIGOELEMENTO  BETWEEN s$elementoInicial$s AND s$elementoFinal$s
  AND INVENTARIO.TIENEMOVIMIENTO NOT IN(0)
  AND INVENTARIO.TIPO            IN(s$tipo$s)
  AND V_ULTIMOMOVIMIENTO_ALM.SALDOKARDEX > 0
)
KARDEXCERO 

LEFT JOIN  

(
SELECT V_MOVIMIENTO.COMPANIA,
       V_MOVIMIENTO.TIPOMOVIMIENTO,
       V_MOVIMIENTO.NUMERO MOVIMIENTO,
       D_MOVIMIENTO.ELEMENTO,
       D_MOVIMIENTO.FECHA,
       D_MOVIMIENTO.HORA,
       D_MOVIMIENTO.SALDOKARDEX,
       D_MOVIMIENTO.VLRUNITARIOPROM,
       D_MOVIMIENTO.SALDOKARDEX * D_MOVIMIENTO.VLRUNITARIOPROM VALORTOTAL,
       D_MOVIMIENTO.VALORUNITARIO,
       D_MOVIMIENTO.VALORSALDO,
       D_MOVIMIENTO.SERIE,
       CASE WHEN V_MOVIMIENTO.CLASE='E' THEN D_MOVIMIENTO.CANTIDAD   ELSE 0 END ENTRADAS,
       CASE WHEN V_MOVIMIENTO.CLASE='E' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END VALENTRADAS,
       CASE WHEN V_MOVIMIENTO.CLASE='S' THEN D_MOVIMIENTO.CANTIDAD   ELSE 0 END SALIDAS,
       CASE WHEN V_MOVIMIENTO.CLASE='S' THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END VALINGRESO1,
       CASE WHEN V_MOVIMIENTO.CLASE='S' THEN D_MOVIMIENTO.VLRUNITARIOPROM * D_MOVIMIENTO.CANTIDAD  ELSE 0 END VALSALIDAS,
       TERCERO.NOMBRE TERCERO,
       DEPENDENCIA.NOMBRE DEPENDENCIA
FROM V_MOVIMIENTO INNER JOIN D_MOVIMIENTO
  ON V_MOVIMIENTO.COMPANIA       = D_MOVIMIENTO.COMPANIA
 AND V_MOVIMIENTO.TIPOMOVIMIENTO = D_MOVIMIENTO.TIPOMOVIMIENTO
 AND V_MOVIMIENTO.NUMERO         = D_MOVIMIENTO.MOVIMIENTO
INNER JOIN TERCERO 
  ON V_MOVIMIENTO.COMPANIA       = TERCERO.COMPANIA
 AND V_MOVIMIENTO.TERCERO        = TERCERO.NIT
 AND V_MOVIMIENTO.SUCURSAL       = TERCERO.SUCURSAL
LEFT JOIN DEPENDENCIA
  ON V_MOVIMIENTO.COMPANIA            = DEPENDENCIA.COMPANIA
 AND V_MOVIMIENTO.DEPENDENCIA_DESTINO = DEPENDENCIA.CODIGO
WHERE D_MOVIMIENTO.COMPANIA = s$compania$s
  AND D_MOVIMIENTO.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND D_MOVIMIENTO.IND_REG NOT IN(0)
  )
C_DMOVIMIENTOKARDEX
  ON KARDEXCERO.COMPANIA       = C_DMOVIMIENTOKARDEX.COMPANIA 
 AND KARDEXCERO.CODIGOELEMENTO = C_DMOVIMIENTOKARDEX.ELEMENTO
 LEFT JOIN  
 (
SELECT COMPANIA,
       TIPOMOVIMIENTO,
       MOVIMIENTO,
       ELEMENTO,
       FECHA,
       HORA,
       VALORUNITARIO,
       VLRUNITARIOPROM,
       SALDOKARDEX,
       SALDOKARDEX * VLRUNITARIOPROM VALORTOTAL,
       VALORSALDO
FROM  V_ULTIMOMOVIMIENTO_ALM_INI
WHERE V_ULTIMOMOVIMIENTO_ALM_INI.COMPANIA = s$compania$s
  AND V_ULTIMOMOVIMIENTO_ALM_INI.ELEMENTO BETWEEN  s$elementoInicial$s AND s$elementoFinal$s
) 
 C_DMOVIMIENTOKARDEXANT
  ON KARDEXCERO.COMPANIA       = C_DMOVIMIENTOKARDEXANT.COMPANIA 
 AND KARDEXCERO.CODIGOELEMENTO = C_DMOVIMIENTOKARDEXANT.ELEMENTO
