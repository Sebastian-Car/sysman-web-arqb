SELECT * FROM (
    SELECT    TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY') VIGENCIA,
              TIPOORDENDECOMPRA.NOMBRE,
              ORDENDECOMPRA.NUMERO
    FROM ORDENDECOMPRA 
    INNER JOIN TIPOORDENDECOMPRA 
        ON  ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO
        AND ORDENDECOMPRA.COMPANIA   = TIPOORDENDECOMPRA.COMPANIA
    WHERE ORDENDECOMPRA.COMPANIA            = 's$compania$s'
      AND ORDENDECOMPRA.CLASEORDEN          = 'CCS'
      AND ORDENDECOMPRA.DEPENDENCIA         = 's$dependencia$s' 
      AND TO_NUMBER(TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY')) 
               BETWEEN s$anioInicial$s  AND s$anioFinal$s
     GROUP BY  TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY'), 
               ORDENDECOMPRA.COMPANIA,
               ORDENDECOMPRA.DEPENDENCIA,
               TIPOORDENDECOMPRA.CODIGO,
               TIPOORDENDECOMPRA.NOMBRE,
               ORDENDECOMPRA.NUMERO
) P PIVOT (COUNT(NUMERO) FOR NOMBRE IN('PRESTACION DE SERVICIOS'))
