SELECT 
   M.TIPOMOVASOCIADO      TIPOM
  ,TO_CHAR(M.MOVASOCIADO) NUMERO
  ,DM2.CANTIDAD           ENTRADA
  ,M.TIPOMOVIMIENTO       TIPOSALIDA
  ,TO_CHAR(M.NUMERO)      NUMEROSALIDA
  ,DM1.CANTIDAD           AFECTADO
FROM MOVIMIENTO M
  INNER JOIN D_MOVIMIENTO DM1
     ON M.COMPANIA       = DM1.COMPANIA
    AND M.TIPOMOVIMIENTO = DM1.TIPOMOVIMIENTO
    AND M.NUMERO         = DM1.MOVIMIENTO
  INNER JOIN D_MOVIMIENTO DM2
     ON M.COMPANIA        = DM2.COMPANIA
    AND M.TIPOMOVASOCIADO = DM2.TIPOMOVIMIENTO
    AND M.MOVASOCIADO     = DM2.MOVIMIENTO
    AND DM1.ELEMENTO      = DM2.ELEMENTO
    AND DM1.CODIGO        = DM2.CODIGO
WHERE M.COMPANIA        = s$compania$s
  AND M.TIPOMOVASOCIADO = s$tipoMovimiento$s
  AND M.MOVASOCIADO     = s$movimiento$s
