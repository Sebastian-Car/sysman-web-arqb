SELECT 
DISTINCT
  INVENTARIO1.COMPANIA,
  I2_INVENTARIOBODEGA_PEPS_C.ELEMENTO CODIGOELEMENTO,
  INVENTARIO1.NOMBRELARGO,
  s$orden2$s ORDEN2,
  s$orden3$s ORDEN3,
  I2_INVENTARIOBODEGA_PEPS_C.TIPO,
  INVENTARIO1.REFERENCIA,
  INVENTARIO1.UNIDAD,
  INVENTARIO1.MEDIDA,
  INVENTARIO1.MARCA,
  INVENTARIO1.LOCALIZACION,
  V_BASE_CUENTAALMACEN.GRUPO SIG,
  '' DEPENDENCIA,
  PCK_ALMACEN.FC_NOMBREINVENTARIO(INVENTARIO1.COMPANIA, V_BASE_CUENTAALMACEN.GRUPO, 0) NOMBREINVENTARIO,
  INVENTARIO1.CANTRESERVADA,
  INVENTARIO1.CANTCOMPORLLEGAR,
  INVENTARIO1.CANTINSERVIBLE,
  LENGTH(I2_INVENTARIOBODEGA_PEPS_C.ELEMENTO) EXPR1,
  I2_INVENTARIOBODEGA_PEPS_C.VLRUNITARIO VALORUNITARIO,
  NVL(I2_INVENTARIOBODEGA_PEPS_C.SALDO_PEPS,0) EXISTENCIA,
  NVL(I2_INVENTARIOBODEGA_PEPS_C.VLRUNITARIO,0) VALOR,
  UNIDAD.NOMBRE, 
  I2_INVENTARIOBODEGA_PEPS_C.VLRUNITARIO*I2_INVENTARIOBODEGA_PEPS_C.SALDO_PEPS VALORTOTAL,
  I2_INVENTARIOBODEGA_PEPS_C.VLRUNITARIO*I2_INVENTARIOBODEGA_PEPS_C.SALDO_PEPS VSALDO,
  NVL(I2_INVENTARIOBODEGA_PEPS_C.SALDO_PEPS,0) EXPR2,
  TO_DATE('s$fechaFinal$s','DD/MM/YYYY') FECHAFINAL
FROM INVENTARIO INVENTARIO1
  LEFT JOIN (SELECT 
              D_MOVIMIENTO.COMPANIA,
              D_MOVIMIENTO.ELEMENTO,
              D_MOVIMIENTO.TIPOMOVIMIENTO,
              D_MOVIMIENTO.MOVIMIENTO, 
              D_MOVIMIENTO.ESTADO,
              D_MOVIMIENTO.FECHA,
              D_MOVIMIENTO.VLRUNITARIOPROM VPROMEDIO,
              D_MOVIMIENTO.SALDO_PEPS,
              D_MOVIMIENTO.VALORUNITARIO VLRUNITARIO,
              INVENTARIO.TIPO,
              SUM(D_MOVIMIENTO.SALDOKARDEX) CSALDO,
              D_MOVIMIENTO.VLRUNITARIOPROM*SUM(D_MOVIMIENTO.SALDOKARDEX) VSALDO
            FROM D_MOVIMIENTO
              INNER JOIN TIPOMOVIMIENTO
                ON  D_MOVIMIENTO.COMPANIA=TIPOMOVIMIENTO.COMPANIA
                AND D_MOVIMIENTO.TIPOMOVIMIENTO=TIPOMOVIMIENTO.CODIGO
              INNER JOIN MOVIMIENTO 
                ON  D_MOVIMIENTO.COMPANIA = MOVIMIENTO.COMPANIA
                AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO
                AND D_MOVIMIENTO.MOVIMIENTO = MOVIMIENTO.NUMERO
              INNER JOIN INVENTARIO 
                ON  D_MOVIMIENTO.COMPANIA = INVENTARIO.COMPANIA
                AND D_MOVIMIENTO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
            WHERE D_MOVIMIENTO.COMPANIA='s$companiaSel$s'
              AND D_MOVIMIENTO.IND_REG NOT IN (0) 
              AND TIPOMOVIMIENTO.CLASE='E'
            GROUP BY 
              D_MOVIMIENTO.COMPANIA, D_MOVIMIENTO.ELEMENTO, D_MOVIMIENTO.TIPOMOVIMIENTO, D_MOVIMIENTO.MOVIMIENTO, D_MOVIMIENTO.ESTADO,
              D_MOVIMIENTO.FECHA, D_MOVIMIENTO.VLRUNITARIOPROM, D_MOVIMIENTO.SALDO_PEPS, D_MOVIMIENTO.VALORUNITARIO, INVENTARIO.TIPO
            ORDER BY D_MOVIMIENTO.COMPANIA DESC, D_MOVIMIENTO.ELEMENTO DESC) I2_INVENTARIOBODEGA_PEPS_C 
    ON  INVENTARIO1.COMPANIA = I2_INVENTARIOBODEGA_PEPS_C.COMPANIA
    AND INVENTARIO1.CODIGOELEMENTO = I2_INVENTARIOBODEGA_PEPS_C.ELEMENTO
  LEFT JOIN UNIDAD 
    ON  INVENTARIO1.UNIDAD = UNIDAD.UNIDAD
  LEFT JOIN V_BASE_CUENTAALMACEN 
    ON  INVENTARIO1.COMPANIA = V_BASE_CUENTAALMACEN.COMPANIA
    AND INVENTARIO1.CODIGOELEMENTO = V_BASE_CUENTAALMACEN.ELEMENTO 
WHERE INVENTARIO1.COMPANIA='s$companiaSel$s'
  AND INVENTARIO1.TIPO='C'
  AND I2_INVENTARIOBODEGA_PEPS_C.ELEMENTO BETWEEN 's$elementoDesde$s' AND 's$elementoHasta$s'
  AND V_BASE_CUENTAALMACEN.FECHA BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
  AND LENGTH(I2_INVENTARIOBODEGA_PEPS_C.ELEMENTO)>=LENGTH(V_BASE_CUENTAALMACEN.GRUPO)
  AND INVENTARIO1.TIENEMOVIMIENTO NOT IN (0)
  s$conSaldoCero$s
ORDER BY I2_INVENTARIOBODEGA_PEPS_C.ELEMENTO
