  DEVOLUTIVO.SERIE PLACA,
  DEVOLUTIVO.ELEMENTO  CODIGO ,
  INVENTARIO.NOMBRELARGO NOMBRE,
  DEVOLUTIVO.SERIEDEVOLUTIVO SERIE,
  TO_CHAR( DEVOLUTIVO.FECHAADQUISICION
  ,'DD/MM/YYYY') "FECHA ADQUISICION",
  TRIM(NOMBRELARGO)||' -'||DEVOLUTIVO.DESCRIPCION AS DESCRIPCION,  
