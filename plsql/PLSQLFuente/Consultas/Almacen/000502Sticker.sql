SELECT 	DEVOLUTIVO.ELEMENTO,  		
        	DEVOLUTIVO.SERIE,  		
       	PCK_CODIGODEBARRAS.FC_IMPRIMIRCODIGODEBARRAS(CHR(205)||CHR(102)||LPAD(DEVOLUTIVO.ELEMENTO,10,'0')||PCK_SYSMAN_UTL.FC_STRZERO(DEVOLUTIVO.SERIE,8)) AS CODIGODEBARRAS_BIN,  
        	LPAD(DEVOLUTIVO.ELEMENTO,10,'0')||PCK_SYSMAN_UTL.FC_STRZERO(DEVOLUTIVO.SERIE,8) BARRAS,
        	INVENTARIO.NOMBRELARGO AS NOMBREELEMENTO,  		
       	DEPENDENCIA.NOMBRE AS NOMBREDEPENDENCIA 
FROM DEVOLUTIVO 
	INNER JOIN INVENTARIO 
		ON  DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA 
		AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO 
	INNER JOIN DEPENDENCIA 
		ON  DEVOLUTIVO.COMPANIA    = DEPENDENCIA.COMPANIA 
		AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO
	s$variableInner$s		
WHERE DEVOLUTIVO.COMPANIA     = s$compania$s 
  AND DEVOLUTIVO.PLACAANULADA = 0
  AND s$condicion$s
ORDER BY s$orden$s
