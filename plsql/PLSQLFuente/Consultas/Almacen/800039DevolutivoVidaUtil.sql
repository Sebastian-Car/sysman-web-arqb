WITH DEPRECIAR2 AS(
SELECT MAX(DEPRECIAR.PERIODO) PERIODO,
       DEPRECIAR.ELEMENTO,
       DEPRECIAR.SERIE,
       DEPRECIAR.COMPANIA
 FROM DEVOLUTIVO
INNER JOIN DEPRECIAR
    ON DEVOLUTIVO.COMPANIA  = DEPRECIAR.COMPANIA
    AND DEVOLUTIVO.ELEMENTO = DEPRECIAR.ELEMENTO
    AND DEVOLUTIVO.SERIE    = DEPRECIAR.SERIE 
GROUP BY   DEPRECIAR.ELEMENTO, 
    DEPRECIAR.SERIE,
    DEPRECIAR.COMPANIA
)
SELECT
           DEVOLUTIVO.ELEMENTO, 
           DEVOLUTIVO.VALOR, 
           TO_CHAR(DEVOLUTIVO.FECHAADQUISICION,'DD/MM/YYYY') FECHACOMPRA, 
           TO_CHAR(DEVOLUTIVO.FECHASALIDASERVICIO,'DD/MM/YYYY') FECHASALIDA, 
           DEVOLUTIVO.MESESVIDAUTILPLACA MESESVIDAUTIL, 
           TO_CHAR(DEVOLUTIVO.SERIE) PLACA, 
           INVENTARIO.NOMBRELARGO NOMBREELEMENTO,
           DEVOLUTIVO.TIPOMOVIMIENTOI TIPOIN, 
           TO_CHAR(DEVOLUTIVO.MOVIMIENTOI) MOVIN, 
           DEVOLUTIVO.DEPACUMULADA, 
           DEVOLUTIVO.VLRLIBROS VALORENLIBROS, 
           DEVOLUTIVO.NIIF_VALOR_TOTAL VALORNIIF, 
           DEVOLUTIVO.SALVAMENTO VALORRESIDUAL, 
           DEVOLUTIVO.DETERIORO, 
           DEVOLUTIVO.NIIF_VALOR_BASE VALORBASENIIF, 
           DEVOLUTIVO.NIIF_VIDA_UTIL VIDAUTILNIIF, 
           TO_CHAR(CASE WHEN DEVOLUTIVO.FECHAADQUISICION < (TO_DATE('31/12/2013','DD/MM/YYYY') + 1) 
                THEN (TO_DATE('31/12/2013','DD/MM/YYYY') + 1)
                ELSE DEVOLUTIVO.NIIF_FECHAFUNCIONAMIENTO
                END,'DD/MM/YYYY') FECHAINICIONIIF, 
           DEVOLUTIVO.NIIF_TIPO_ACTIVO, 
           DEVOLUTIVO.APLICA_NIIF,
           TO_CHAR(DEPRECIAR2.PERIODO
,'DD/MM/YYYY') ULTIMOPERIODODEP
 FROM  DEVOLUTIVO 
     INNER JOIN INVENTARIO 
        ON DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA 
       AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO 
     LEFT JOIN DEPRECIAR2
        ON DEVOLUTIVO.COMPANIA  = DEPRECIAR2.COMPANIA       
        AND DEVOLUTIVO.ELEMENTO = DEPRECIAR2.ELEMENTO    
        AND DEVOLUTIVO.SERIE    = DEPRECIAR2.SERIE 
WHERE DEVOLUTIVO.COMPANIA        = s$compania$s
  AND DEVOLUTIVO.FECHAADQUISICION <= s$fechaCorte$s 
  AND (DEVOLUTIVO.FECHAANULADA    > s$fechaCorte$s             
                      OR DEVOLUTIVO.FECHAANULADA IS NULL) 
   s$condicionReporte$s   
  ORDER BY 2,3
