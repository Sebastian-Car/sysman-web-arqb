SELECT DISTINCT 
  	ORDENDESUMINISTRO.NUMERO ORDENDESUMINISTRO_NUMERO,
	TO_CHAR(ORDENDESUMINISTRO.FECHA,'DD/MM/YYYY') ORDENDESUMINISTRO_FECHA, 
	TIPOORDENDECOMPRA.NOMBRE TIPOORDENDECOMPRA_NOMBRE, 
	D_ORDENDECOMPRA.CODIGO CODIGO, 
	TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY')  ORDENDECOMPRA_FECHA, 
	TIPOMOVIMIENTO.NOMBRE  TIPOMOVIMIENTO_NOMBRE,
	TO_CHAR(MOVIMIENTO.FECHA,'DD/MM/YYYY') MOVIMIENTO_FECHA, 
	TO_CHAR(D_MOVIMIENTO.FECHAENTRADA,'DD/MM/YYYY') FECHAENTRADA,
	MOVIMIENTO.NUMERO MOVIMIENTO_NUMERO
FROM ORDENDESUMINISTRO LEFT JOIN D_MOVIMIENTO 
	ON ORDENDESUMINISTRO.COMPANIA = D_MOVIMIENTO.COMPANIA 
	AND ORDENDESUMINISTRO.NUMERO = D_MOVIMIENTO.ORDENDESUMINISTRO 
LEFT JOIN TIPOMOVIMIENTO 
	ON D_MOVIMIENTO.COMPANIA = TIPOMOVIMIENTO.COMPANIA
	AND D_MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO 
LEFT JOIN D_ORDENDECOMPRA 
	ON ORDENDESUMINISTRO.COMPANIA = D_ORDENDECOMPRA.COMPANIA
	AND ORDENDESUMINISTRO.NUMERO = D_ORDENDECOMPRA.ORDENDESUMINISTRO 
LEFT JOIN TIPOORDENDECOMPRA 
	ON D_ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA
	AND D_ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO 
LEFT JOIN MOVIMIENTO 
	ON D_MOVIMIENTO.COMPANIA = MOVIMIENTO.COMPANIA
	AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO
	AND D_MOVIMIENTO.MOVIMIENTO = MOVIMIENTO.NUMERO 
LEFT JOIN ORDENDECOMPRA 
	ON D_ORDENDECOMPRA.COMPANIA = ORDENDECOMPRA.COMPANIA
	AND D_ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRA.CLASEORDEN
	AND D_ORDENDECOMPRA.ORDENDECOMPRA = ORDENDECOMPRA.NUMERO
WHERE ORDENDESUMINISTRO.COMPANIA=s$compania$s
	AND ORDENDESUMINISTRO.FECHA BETWEEN  's$fecha_inicial$s'  AND  's$fecha_final$s'   
	AND  ORDENDESUMINISTRO.NUMERO= s$numero_orden$s