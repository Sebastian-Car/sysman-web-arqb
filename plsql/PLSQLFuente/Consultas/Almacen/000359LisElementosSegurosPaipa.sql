SELECT DISTINCT  
 
        DEVOLUTIVO.SERIE PLACA,  
 
        INVENTARIO.NOMBRELARGO,  
 
        DEPENDENCIA.NOMBRE DEPENDENCIA,  
 
        TERCERO.NOMBRE RESPONSABLE,  
 
        DEVOLUTIVO.VALOR       
 
FROM INVENTARIO  
 
  RIGHT JOIN DEVOLUTIVO  
 
          ON INVENTARIO.CODIGOELEMENTO  = DEVOLUTIVO.ELEMENTO 
 
          AND INVENTARIO.COMPANIA       = DEVOLUTIVO.COMPANIA 
 
  LEFT JOIN DEPENDENCIA  
 
          ON DEVOLUTIVO.COMPANIA        = DEPENDENCIA.COMPANIA 
 
          AND DEVOLUTIVO.DEPENDENCIA    = DEPENDENCIA.CODIGO 
 
  LEFT JOIN TERCERO  
 
          ON DEVOLUTIVO.COMPANIA        		= TERCERO.COMPANIA  
 
          AND DEVOLUTIVO.RESPONSABLE    		= TERCERO.NIT 
 
          AND DEVOLUTIVO.SUCURSAL_RESPONSABLE    	= TERCERO.SUCURSAL 
 
WHERE INVENTARIO.TIPO = 's$tipo$s'  
 
  AND DEVOLUTIVO.PLACAANULADA NOT IN (-1)  
 
  AND DEVOLUTIVO.COMPANIA = s$compania$s 
 
   s$strOrderBy$s
