SELECT 
  COMPANIA ,
  TIPOMOVIMIENTO ,
  MOVIMIENTO NUMERO ,
  CODIGO ,
  ELEMENTO ,
  SERIEDEVOLUTIVO ,
  SERIE ,
  TIPOMOVIMIENTO_NOM ,
  TO_CHAR(FECHA_MOV, 'DD')
  || ' de '
  || PCK_SYSMAN_UTL.FC_NOMBRE_MES(TO_CHAR(FECHA_MOV, 'MM'))
  || ' de '
  || TO_CHAR(FECHA_MOV, 'YYYY') FECHA ,
  MOVASOCIADO ,
  FECHAMOVASOCIADO ,
  RESPONSABLE_ORIGEN_NOM ,
  FACTURANO ,
  UNIDAD_NOM ,
  CANTIDAD ,
  NOMBRELARGO
  ||
  CASE
    WHEN TRIM(NVL(V_MOVIMIENTO_INFORME.ESPECIFICACION,'')) IS NULL
    THEN ' '
    ELSE ', '
      || V_MOVIMIENTO_INFORME.ESPECIFICACION
  END
  ||
  CASE
    WHEN TRIM(NVL(V_MOVIMIENTO_INFORME.MARCA,'')) IS NOT NULL
    THEN ', MARCA: '
      || V_MOVIMIENTO_INFORME.MARCA
      || ' '
    ELSE ''
  END
  ||
  CASE
    WHEN TRIM(NVL(V_MOVIMIENTO_INFORME.SERIEDEVOLUTIVO,'')) IS NOT NULL
    THEN ', SERIE: '
      || V_MOVIMIENTO_INFORME.SERIEDEVOLUTIVO
      || ' '
    ELSE ''
  END
  ||
  CASE
    WHEN TRIM(NVL(V_MOVIMIENTO_INFORME.SERIE,'')) = 0
    THEN ' '
    ELSE ', PLACA: '
      || V_MOVIMIENTO_INFORME.SERIE
  END DESCRIPCIONELEMENTO ,
  VALORUNITARIO ,
  VALORTOTAL_D VALORTOTAL ,
  AJUSTECENTAVOS1_MOV * (-1) AJUSTECENTAVOS ,
  TOTALCONAJUSTE_MOV TOTALCONAJUSTE ,
  DESCRIPCION_MOV DESCRIPCION ,
  (TIPOMOVIMIENTO || MOVIMIENTO) GRUPO
FROM V_MOVIMIENTO_INFORME
WHERE COMPANIA     = s$compania$s
AND TIPOMOVIMIENTO = 's$tipoMovimiento$s'
AND MOVIMIENTO BETWEEN s$movimientoInicial$s AND s$movimientoFinal$s
ORDER BY ELEMENTO,
  SERIE