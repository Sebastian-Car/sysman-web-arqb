MERGE INTO CONSULTAS FIN USING (SELECT '002239requisicionesporlote' INFORME ,TO_CLOB(q'[SELECT  D_ORDENDESUMINISTRO.ORDENDESUMINISTRO,
        ORDENDESUMINISTRO.TERCERO,
        TO_CHAR(ORDENDESUMINISTRO.FECHA,'DD/MM/YYYY') FECHA,
        ORDENDESUMINISTRO.DESCRIPCION,
        ORDENDESUMINISTRO.OBSERVACIONES,
        ORDENDESUMINISTRO.VALORESTIMADO,
        D_ORDENDESUMINISTRO.ELEMENTO,
        DEPENDENCIA.NOMBRE DEPENDENCIANOMBRE,
        D_ORDENDESUMINISTRO.CANTIDADAPROBADA,
                                           D_ORDENDESUMINISTRO.CANTIDAD,
        INVENTARIO.UNIDAD,
                                           D_ORDENDESUMINISTRO.ESPECIFICACION,
        TERCERO.NOMBRE RESPONSABLENOMBRE,
        ORDENDESUMINISTRO.TERCERO,
        DEPENDENCIA_RESPONSABLE.CARGO,
        UNIDAD.NOMBRE UNIDADNOMBRE,
        (CASE WHEN D_ORDENDESUMINISTRO.ELEMENTO <> '9999999999999999'
              THEN INVENTARIO.NOMBRELARGO||D_ORDENDESUMINISTRO.ESPECIFICACION
              ELSE D_ORDENDESUMINISTRO.ESPECIFICACION
                                           END) NOMBREELEMENTO
FROM D_ORDENDESUMINISTRO
    INNER JOIN ORDENDESUMINISTRO
        ON  D_ORDENDESUMINISTRO.COMPANIA           = ORDENDESUMINISTRO.COMPANIA
        AND D_ORDENDESUMINISTRO.ORDENDESUMINISTRO = ORDENDESUMINISTRO.NUMERO
    INNER JOIN INVENTARIO
        ON  D_ORDENDESUMINISTRO.COMPANIA = INVENTARIO.COMPANIA
        AND D_ORDENDESUMINISTRO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
         LEFT JOIN UNIDAD
              ON INVENTARIO.UNIDAD = UNIDAD.UNIDAD
    INNER JOIN DEPENDENCIA_RESPONSABLE
        ON  ORDENDESUMINISTRO.COMPANIA    = DEPENDENCIA_RESPONSABLE.COMPANIA
        AND ORDENDESUMINISTRO.DEPENDENCIA = DEPENDENCIA_RESPONSABLE.DEPENDENCIA
        AND ORDENDESUMINISTRO.TERCERO     = DEPENDENCIA_RESPONSABLE.RESPONSABLE
    INNER JOIN DEPENDENCIA
        ON  DEPENDENCIA_RESPONSABLE.COMPANIA = DEPENDENCIA.COMPANIA
        AND DEPENDENCIA_RESPONSABLE.DEPENDENCIA = DEPENDENCIA.CODIGO
          INNER JOIN RESPONSABLE
            ON  DEPENDENCIA_RESPONSABLE.COMPANIA    = RESPONSABLE.COMPANIA
            AND DEPENDENCIA_RESPONSABLE.RESPONSABLE = RESPONSABLE.CEDULA
            AND DEPENDENCIA_RESPONSABLE.SUCURSAL = RESPONSABLE.SUCURSAL
        INNER JOIN TERCERO
            ON  RESPONSABLE.COMPANIA = TERCERO.COMPANIA
            AND RESPONSABLE.CEDULA   = TERCERO.NIT
            AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
WHERE D_ORDENDESUMINISTRO.COMPANIA   = s$compania$s
  AND D_ORDENDESUMINISTRO.ORDENDESUMINISTRO=s$ordendesuministro$s
ORDER BY D_ORDENDESUMINISTRO.COMPANIA,
D_ORDENDESUMINISTRO.ORDENDESUMINISTRO,
D_ORDENDESUMINISTRO.CODIGO]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);