      ' Marca: '|| DEV.MARCA END ||
    CASE WHEN  TRIM(DEV.SERIEDEVOLUTIVO) IS NULL THEN '' ELSE
      ' Serie: '|| DEV.SERIEDEVOLUTIVO 
    END  DESCRIPCION,
  VMOV.TIPOMOVIMIENTO,
  VMOV.ELEMENTO CODIGO,
