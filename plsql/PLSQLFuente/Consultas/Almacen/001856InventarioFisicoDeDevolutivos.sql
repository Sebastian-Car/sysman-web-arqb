MERGE INTO CONSULTAS FIN USING (SELECT '001856InventarioFisicoDeDevolutivos' INFORME ,TO_CLOB(q'[SELECT DEVOLUTIVO.COMPANIA,
             DEVOLUTIVO.SERIEDEVOLUTIVO,
	         DEVOLUTIVO.DEPENDENCIA,
	         DEVOLUTIVO.SERIE,
             DEVOLUTIVO.ESTADO,
             DEVOLUTIVO.VALOR                         TVALOR,
             DEVOLUTIVO.SERIEANTERIOR                 PLACAANTERIOR,
                TO_CHAR(DEVOLUTIVO.FECHAADQUISICION, 'DD/MM/YYYY') FECHAADQUISICION,
			 SUBSTR(DEVOLUTIVO.ELEMENTO, 0,s$digito$s) AGRUPACION, 
			 DEVOLUTIVO.DESCRIPCION                   DESCRIPCIOND,
			 ESTADODEVOLUTIVO.DESCRIPCION    		    DESCRIPCIONE,
			 DEPENDENCIA.NOMBRE,
			 DEVOLUTIVO.ELEMENTO,
			 INVENTARIO.NOMBRELARGO,
			 INV.NOMBRELARGO                          GRUPO     
 FROM  DEVOLUTIVO
 INNER  JOIN INVENTARIO
	  ON  DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA
    AND  DEVOLUTIVO.ELEMENTO  = INVENTARIO.CODIGOELEMENTO
 INNER  JOIN DEPENDENCIA
      ON  DEVOLUTIVO.COMPANIA   = DEPENDENCIA.COMPANIA
    AND  DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO
 INNER  JOIN ESTADODEVOLUTIVO
      ON  DEVOLUTIVO.ESTADO = ESTADODEVOLUTIVO.ESTADO
 INNER  JOIN INVENTARIO INV
 	  ON DEVOLUTIVO.COMPANIA = INV.COMPANIA
     AND SUBSTR(DEVOLUTIVO.ELEMENTO, 0,s$digito$s)   = INV.CODIGOELEMENTO
WHERE DEVOLUTIVO.COMPANIA                = s$compania$s
   AND   DEVOLUTIVO.FECHAADQUISICION     <=  s$txtFecha$s 
   AND   (PLACAANULADA=0 OR  ( PLACAANULADA=-1 AND  FECHAANULADA>s$txtFecha$s))
   AND   SUBSTR(DEVOLUTIVO.ELEMENTO, 0,s$digito$s)  = INV.CODIGOELEMENTO
   AND   DEVOLUTIVO.VALOR <> 0  ORDER BY   TO_CHAR(DEVOLUTIVO.FECHAADQUISICION, 'DD/MM/YYYY')]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);