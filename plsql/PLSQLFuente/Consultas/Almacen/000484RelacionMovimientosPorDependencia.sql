SELECT D_MOVIMIENTO.COMPANIA, 
    D_MOVIMIENTO.TIPOMOVIMIENTO, 
    V_MOVIMIENTOS_TOTALES.NUMERO AS CONSECUTIVO, 
    TO_CHAR(D_MOVIMIENTO.FECHA,'DD/MM/YYYY') FECHA,
    TO_CHAR(MAX(D_MOVIMIENTO.HORA),'HH:MM:SS AM') PRIMERODEHORA, 
    V_MOVIMIENTOS_TOTALES.RESPONSABLE_ORIGEN RESPONSABLEANT,  
    V_MOVIMIENTOS_TOTALES.DEPENDENCIA_ORIGEN DEPENDENCIAANT,
    V_MOVIMIENTOS_TOTALES.RESPONSABLE_DESTINO RESPONSABLENUE, 
    V_MOVIMIENTOS_TOTALES.DEPENDENCIA_DESTINO DEPENDENCIANUE, 
    V_MOVIMIENTOS_TOTALES.RESPONSABLE_ORIGEN_NOM  NOMRESANT, 
    V_MOVIMIENTOS_TOTALES.DEPENDENCIA_ORIGEN_NOM  NOMDEPANT, 
    V_MOVIMIENTOS_TOTALES.RESPONSABLE_DESTINO_NOM  NOMRESNUE, 
    V_MOVIMIENTOS_TOTALES.DEPENDENCIA_DESTINO_NOM  NOMDEPNUE,
    V_MOVIMIENTOS_TOTALES.VALORTOTAL VALOR, 
    DECODE(D_MOVIMIENTO.IND_REG,0,' ','X') IND_REG
FROM  V_MOVIMIENTOS_TOTALES INNER JOIN D_MOVIMIENTO ON 
        V_MOVIMIENTOS_TOTALES.COMPANIA = D_MOVIMIENTO.COMPANIA 
        AND V_MOVIMIENTOS_TOTALES.TIPOMOVIMIENTO = D_MOVIMIENTO.TIPOMOVIMIENTO      
        AND V_MOVIMIENTOS_TOTALES.NUMERO = D_MOVIMIENTO.MOVIMIENTO 
    INNER JOIN TIPOMOVIMIENTO ON 
        V_MOVIMIENTOS_TOTALES.COMPANIA = TIPOMOVIMIENTO.COMPANIA 
        AND V_MOVIMIENTOS_TOTALES.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
WHERE V_MOVIMIENTOS_TOTALES.COMPANIA = s$compania$s
    AND ((D_MOVIMIENTO.FECHA BETWEEN s$desde$s AND s$hasta$s
            AND V_MOVIMIENTOS_TOTALES.DEPENDENCIA_ORIGEN = 's$dependencia$s') 
        OR
        (D_MOVIMIENTO.FECHA BETWEEN s$desde$s AND s$hasta$s
            AND V_MOVIMIENTOS_TOTALES.DEPENDENCIA_DESTINO = 's$dependencia$s'))
GROUP BY  D_MOVIMIENTO.COMPANIA, 
    D_MOVIMIENTO.TIPOMOVIMIENTO, 
    D_MOVIMIENTO.MOVIMIENTO, 
    V_MOVIMIENTOS_TOTALES.NUMERO, 
    TO_CHAR(D_MOVIMIENTO.FECHA,'DD/MM/YYYY'), 
    V_MOVIMIENTOS_TOTALES.RESPONSABLE_ORIGEN, 
    V_MOVIMIENTOS_TOTALES.DEPENDENCIA_ORIGEN, 
    V_MOVIMIENTOS_TOTALES.RESPONSABLE_DESTINO, 
    V_MOVIMIENTOS_TOTALES.DEPENDENCIA_DESTINO, 
    V_MOVIMIENTOS_TOTALES.RESPONSABLE_ORIGEN_NOM, 
    V_MOVIMIENTOS_TOTALES.DEPENDENCIA_ORIGEN_NOM, 
    V_MOVIMIENTOS_TOTALES.RESPONSABLE_DESTINO_NOM, 
    V_MOVIMIENTOS_TOTALES.DEPENDENCIA_DESTINO_NOM,
    V_MOVIMIENTOS_TOTALES.VALORTOTAL, 
    DECODE(D_MOVIMIENTO.IND_REG,0,' ','X')