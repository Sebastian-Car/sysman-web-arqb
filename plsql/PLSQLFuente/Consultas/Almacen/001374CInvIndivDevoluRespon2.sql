WITH MOVINVENTARIO AS
(
SELECT 
  D_MOVIMIENTO.COMPANIA,
  D_MOVIMIENTO.TIPOMOVIMIENTO, 
  D_MOVIMIENTO.MOVIMIENTO, 
  D_MOVIMIENTO.ELEMENTO, 
  D_MOVIMIENTO.SERIE, 
  TO_CHAR(D_MOVIMIENTO.FECHA,'DD/MM/YYYY')||TO_CHAR(D_MOVIMIENTO.HORA,'HH24:MI:SS') UFECHA,
  D_MOVIMIENTO.FECHA, 
  D_MOVIMIENTO.HORA, 
  MOVIMIENTO.TERCERO RESPONSABLE, 
  MOVIMIENTO.SUCURSAL,
  MOVIMIENTO.DEPENDENCIA_DESTINO DEPENDENCIA, 
  D_MOVIMIENTO.VALORUNITARIO 
FROM V_MOVIMIENTO MOVIMIENTO 
  INNER JOIN D_MOVIMIENTO 
    ON  MOVIMIENTO.COMPANIA       = D_MOVIMIENTO.COMPANIA
    AND MOVIMIENTO.TIPOMOVIMIENTO = D_MOVIMIENTO.TIPOMOVIMIENTO 
    AND MOVIMIENTO.NUMERO         = D_MOVIMIENTO.MOVIMIENTO 
WHERE D_MOVIMIENTO.COMPANIA = s$compania$s
  AND TRUNC(D_MOVIMIENTO.FECHA)    <= TO_DATE('s$fecha$s', 'DD/MM/YYYY') 
  AND D_MOVIMIENTO.IND_REG  NOT IN(0)
)
,
MOVINVENTARIOAGRU AS(
SELECT MOVINVENTARIO.COMPANIA, 
       MOVINVENTARIO.ELEMENTO, 
       MOVINVENTARIO.SERIE, 
       MAX(MOVINVENTARIO.UFECHA) MAXIMO
FROM MOVINVENTARIO 
GROUP BY MOVINVENTARIO.COMPANIA, 
         MOVINVENTARIO.ELEMENTO, 
         MOVINVENTARIO.SERIE
         )
, INVENTARIOINDIVIDUAL2 AS 
(SELECT 
  MOVINVENTARIO.COMPANIA,
  MOVINVENTARIO.ELEMENTO,
  MOVINVENTARIO.SERIE,
  DEVOLUTIVO.ESTADO,
  DEVOLUTIVO.SERIEDEVOLUTIVO,
  MOVINVENTARIO.FECHA,
  CASE
    WHEN (TIPOMOVIMIENTO.CLASE  ='E' AND TIPOMOVIMIENTO.CONCEPTO ='R')
    OR TIPOMOVIMIENTO.TIPOPESONA='P'
    THEN PCK_SYSMAN_UTL.FC_PAR(MOVINVENTARIOAGRU.COMPANIA,'CEDULA RESPONSABLE ALMACEN',PCK_DATOS.FC_MODULOALMACEN,SYSDATE)
    ELSE MOVINVENTARIO.RESPONSABLE
  END RESPONSABLE,
  CASE
    WHEN (TIPOMOVIMIENTO.CLASE  ='E' AND TIPOMOVIMIENTO.CONCEPTO ='R')
    OR TIPOMOVIMIENTO.TIPOPESONA='P'
    THEN PCK_SYSMAN_UTL.FC_PAR(MOVINVENTARIOAGRU.COMPANIA,'SUCURSAL RESPONSABLE ALMACEN',PCK_DATOS.FC_MODULOALMACEN,SYSDATE)
    ELSE MOVINVENTARIO.SUCURSAL
  END SUCURSAL,
  CASE
    WHEN TIPOMOVIMIENTO.CLASE = 'E' AND TIPOMOVIMIENTO.CONCEPTO ='R'
    THEN PCK_SYSMAN_UTL.FC_PAR(MOVINVENTARIOAGRU.COMPANIA,'BODEGA ALMACEN',PCK_DATOS.FC_MODULOALMACEN,SYSDATE)
    ELSE MOVINVENTARIO.DEPENDENCIA
  END DEPENDENCIA,
  MOVINVENTARIO.VALORUNITARIO TVALOR,
  INVENTARIO.CODIGOELEMENTO,
  INVENTARIO.UNIDAD,
  ESTADODEVOLUTIVO.DESCRIPCION,
  CASE
    WHEN DEVOLUTIVO.MARCA IS NULL
    THEN ''
    ELSE 'Marca: '||DEVOLUTIVO.MARCA||' '||DEVOLUTIVO.DESCRIPCION
  END NOMBRELARGO,
  SUBSTR(DEVOLUTIVO.ELEMENTO,0,3) AGRUPACION,
  INVENTARIO.NOMBRELARGO NOMELEMENTO
FROM MOVINVENTARIOAGRU
  INNER JOIN MOVINVENTARIO
    ON MOVINVENTARIOAGRU.COMPANIA  = MOVINVENTARIO.COMPANIA
    AND MOVINVENTARIOAGRU.MAXIMO   = MOVINVENTARIO.UFECHA
    AND MOVINVENTARIOAGRU.ELEMENTO = MOVINVENTARIO.ELEMENTO
    AND MOVINVENTARIOAGRU.SERIE    = MOVINVENTARIO.SERIE
  LEFT JOIN INVENTARIO
    ON  MOVINVENTARIOAGRU.COMPANIA  = INVENTARIO.COMPANIA
    AND MOVINVENTARIOAGRU.ELEMENTO = INVENTARIO.CODIGOELEMENTO
  INNER JOIN DEVOLUTIVO
    ON  MOVINVENTARIOAGRU.COMPANIA = DEVOLUTIVO.COMPANIA
    AND MOVINVENTARIOAGRU.ELEMENTO = DEVOLUTIVO.ELEMENTO   
    AND MOVINVENTARIOAGRU.SERIE    = DEVOLUTIVO.SERIE  
  LEFT JOIN ESTADODEVOLUTIVO 
    ON DEVOLUTIVO.ESTADO = ESTADODEVOLUTIVO.ESTADO  
  LEFT JOIN UNIDAD 
    ON INVENTARIO.UNIDAD = UNIDAD.UNIDAD 
  INNER JOIN TIPOMOVIMIENTO 
    ON  MOVINVENTARIO.COMPANIA       = TIPOMOVIMIENTO.COMPANIA 
    AND MOVINVENTARIO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO 
WHERE (PLACAANULADA IN (0) 
      AND CASE WHEN (TIPOMOVIMIENTO.CLASE='E' AND TIPOMOVIMIENTO.CONCEPTO='R') 
                        OR TIPOMOVIMIENTO.TIPOPESONA='P'
                THEN PCK_SYSMAN_UTL.FC_PAR(MOVINVENTARIOAGRU.COMPANIA,'CEDULA RESPONSABLE ALMACEN',PCK_DATOS.FC_MODULOALMACEN,SYSDATE)
                ELSE MOVINVENTARIO.RESPONSABLE
           END='s$responsable$s') 
    OR (PLACAANULADA NOT IN (0) 
       AND CASE WHEN (TIPOMOVIMIENTO.CLASE='E' AND TIPOMOVIMIENTO.CONCEPTO='R') 
                        OR TIPOMOVIMIENTO.TIPOPESONA='P'
                THEN PCK_SYSMAN_UTL.FC_PAR(MOVINVENTARIOAGRU.COMPANIA,'CEDULA RESPONSABLE ALMACEN',PCK_DATOS.FC_MODULOALMACEN,SYSDATE)
                  ELSE MOVINVENTARIO.RESPONSABLE
             END='s$responsable$s'
             ) 
       AND DEVOLUTIVO.FECHAANULADA>TO_DATE('s$fecha$s', 'DD/MM/YYYY')   
ORDER BY INVENTARIO.NOMBRELARGO
)
SELECT  
  INVENTARIOINDIVIDUAL2.SERIE, 
  INVENTARIOINDIVIDUAL2.ESTADO, 
  INVENTARIOINDIVIDUAL2.SERIEDEVOLUTIVO, 
  INVENTARIOINDIVIDUAL2.RESPONSABLE, 
  INVENTARIOINDIVIDUAL2.DEPENDENCIA, 
  INVENTARIOINDIVIDUAL2.TVALOR,  
  INVENTARIOINDIVIDUAL2.CODIGOELEMENTO, 
  INVENTARIOINDIVIDUAL2.UNIDAD, 
  INVENTARIOINDIVIDUAL2.DESCRIPCION, 
  INVENTARIOINDIVIDUAL2.NOMBRELARGO, 
  INVENTARIOINDIVIDUAL2.AGRUPACION, 
  DEPENDENCIA.NOMBRE DEP_NOMBRE, 
  TERCERO.NOMBRE RESP_NOMBRE, 
  INVENTARIOINDIVIDUAL2.NOMELEMENTO,  
  PCK_ALMACEN.FC_NOMBREINVENTARIO(INVENTARIOINDIVIDUAL2.COMPANIA,INVENTARIOINDIVIDUAL2.AGRUPACION,0) NOM_AGRUPACION  
FROM INVENTARIOINDIVIDUAL2 
  LEFT JOIN DEPENDENCIA 
    ON  INVENTARIOINDIVIDUAL2.COMPANIA    = DEPENDENCIA.COMPANIA
    AND INVENTARIOINDIVIDUAL2.DEPENDENCIA = DEPENDENCIA.CODIGO 
LEFT JOIN TERCERO
  ON  INVENTARIOINDIVIDUAL2.COMPANIA    = TERCERO.COMPANIA
  AND INVENTARIOINDIVIDUAL2.RESPONSABLE = TERCERO.NIT  
  AND INVENTARIOINDIVIDUAL2.SUCURSAL    = TERCERO.SUCURSAL  
