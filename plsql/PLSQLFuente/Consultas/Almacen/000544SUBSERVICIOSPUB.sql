SELECT  PREDIOS.ID_PREDIO, 
              SERVICIOS_PUBLICOS.NOMBRE
 FROM    PREDIOS 
 LEFT JOIN (SERVICIOS_PREDIO 
      LEFT JOIN SERVICIOS_PUBLICOS ON (SERVICIOS_PREDIO.COMPANIA = SERVICIOS_PUBLICOS.COMPANIA 
                                   AND SERVICIOS_PREDIO.CODIGO_SERVICIO = SERVICIOS_PUBLICOS.CODIGO_SERVICIO )) 
                                   ON (PREDIOS.COMPANIA = SERVICIOS_PREDIO.COMPANIA 
                                   AND PREDIOS.ID_PREDIO = SERVICIOS_PREDIO.ID_PREDIO)
 WHERE PREDIOS.COMPANIA = s$compania$s
 AND   PREDIOS.ID_PREDIO = $P{PR_ID_PREDIO}
