SELECT TD.COMPANIA, 
       TD.TIPO,
       TTD.NOMBRE NOM_TIPO,
       TD.CODIGO, 
       TD.TIPO_SOLICITANTE,
       TS.NOMBRE NOM_TIPO_SOLICITANTE,
       TD.DOCUMENTO_SOLICITANTE, 
       TD.DEPENDENCIA_SOLICITANTE,
       DEPENDENCIA.NOMBRE NOM_DEPENDENCIA_SOLICITANTE,
       TD.DESCRIPCION, 
       TD.NUMDOCUMENTO_REPRESENTANTE, 
       TD.RESPUESTA, 
       SOL.NOMBRE1 NOM1_SOLICITANTE, 
       SOL.NOMBRE2 NOM2_SOLICITANTE, 
       SOL.APELLIDO1 APE1_SOLICITANTE, 
       SOL.APELLIDO2 APE2_SOLICITANTE, 
       SOL.TELEFONOS TEL_SOLICITANTE, 
       SOL.DIRECCIONEMAIL EMAIL_SOLICITANTE, 
       SOL.CODIGOPOSTAL, 
       SOL.PAIS,
       PAISES.NOMBRE NOM_PAIS,
       SOL.DEPARTAMENTO, 
       DEPARTAMENTO.NOMBRE NOM_DEPARTAMENTO,
       SOL.CIUDAD,
       CIUDAD.NOMBRE NOM_CIUDAD,
       SOL.DIRECCION, 
       REP.NOMBRE1 NOM1_REPRESENTANTE, 
       REP.DIRECCIONEMAIL EMAIL_REPRESENTANTE, 
       REP.NOMBRE2 NOM2_REPRESENTANTE, 
       REP.APELLIDO1 APE1_REPRESENTANTE, 
       REP.APELLIDO2 APE2_REPRESENTANTE, 
       REP.TELEFONOS TEL_REPRESENTANTE, 
       SOL.TIPOID TIPODOC_SOLICITANTE, 
       TIPOS_DOCUMENTOS.DESCRIPCION NOM_TIPODOC_SOLICITANTE,
       REP.TIPOID TIPODOC_REPRESENTANTE,  
       TDR.DESCRIPCION NOM_TIPODOC_REPRESENTANTE
FROM   TERCERO SOL 
       LEFT JOIN PAISES
          ON  SOL.PAIS = PAISES.PAIS
       LEFT JOIN DEPARTAMENTO
          ON  SOL.PAIS         = DEPARTAMENTO.PAIS
          AND SOL.DEPARTAMENTO = DEPARTAMENTO.CODIGO
       LEFT JOIN CIUDAD
          ON  SOL.PAIS          = CIUDAD.PAIS
          AND SOL.DEPARTAMENTO  = CIUDAD.DEPARTAMENTO
          AND SOL.CIUDAD        = CIUDAD.CODIGO
       INNER JOIN TIPOS_DOCUMENTOS
          ON  SOL.COMPANIA = TIPOS_DOCUMENTOS.COMPANIA
          AND SOL.TIPOID   = TIPOS_DOCUMENTOS.DCTO_IDENTIDAD
       INNER JOIN TRANSACCION_DEVOLUTIVO TD
          ON  SOL.COMPANIA = TD.COMPANIA
          AND SOL.NIT      = TD.DOCUMENTO_SOLICITANTE 
          AND SOL.SUCURSAL = TD.SUCURSAL_SOLICITANTE 
       INNER JOIN TIPO_TRANSACCION_DEV TTD
          ON  TD.COMPANIA = TTD.COMPANIA
          AND TD.TIPO     = TTD.CODIGO
       INNER JOIN TIPO_SOLICITANTE TS
          ON  TD.COMPANIA         = TS.COMPANIA
          AND TD.TIPO_SOLICITANTE = TS.CODIGO
       LEFT JOIN DEPENDENCIA
          ON  TD.COMPANIA                = DEPENDENCIA.COMPANIA
          AND TD.DEPENDENCIA_SOLICITANTE = DEPENDENCIA.CODIGO
       LEFT JOIN TERCERO REP 
          ON  TD.COMPANIA                   = REP.COMPANIA 
          AND TD.NUMDOCUMENTO_REPRESENTANTE = REP.NIT
          AND TD.SUCURSAL_REPRESENTANTE     = REP.SUCURSAL
       LEFT JOIN TIPOS_DOCUMENTOS TDR
          ON  REP.COMPANIA = TDR.COMPANIA
          AND REP.TIPOID   = TDR.DCTO_IDENTIDAD
WHERE  TD.COMPANIA = s$compania$s
  AND  TD.TIPO     = 's$tipoTransaccion$s'
  AND  TD.CODIGO   = s$codigoTransaccion$s
  AND  TD.ESTADO   = 'A'
