MERGE INTO CONSULTAS FIN USING (SELECT '800551REPORTEMENSUALSALIDAS' INFORME ,TO_CLOB(q'[SELECT M.ANO,
M.TIPOMOVIMIENTO,
M.NUMERO,
M.BODEGA_ORIGEN,
M.CODIGOPROYECTO,
M.VALORTOTAL,
M.FECHA,
U.CEDULA,
T.CLASE,
(U.NOMBRE1 || ' ' || U.NOMBRE2 || ' ' || U.APELLIDO1 || ' ' || U.APELLIDO2) NOMBRE_USUARIO
FROM MOVIMIENTO M INNER JOIN USUARIO U 
ON M.CREATED_BY = U.CODIGO
INNER JOIN TIPOMOVIMIENTO T 
ON M.COMPANIA = T.COMPANIA
AND M.TIPOMOVIMIENTO = T.CODIGO
WHERE M.COMPANIA = s$compania$s
AND T.CLASE = 'S'
AND M.FECHA BETWEEN TO_DATE('s$fechaini$s','DD/MM/YYYY') AND TO_DATE('s$fechafin$s','DD/MM/YYYY')
ORDER BY 
M.NUMERO ASC, 
M.TIPOMOVIMIENTO]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);