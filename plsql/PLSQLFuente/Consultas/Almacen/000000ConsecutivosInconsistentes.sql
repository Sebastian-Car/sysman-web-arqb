SELECT 
INVENTARIO.CODIGOELEMENTO "CODIGO ELEMENTO", 
DEVOLUTIVO.SERIE, INVENTARIO.CODIGOELEMENTO  "NUEVO ELEMENTO", 
0  "NUEVA SERIE"
FROM INVENTARIO INNER JOIN DEVOLUTIVO 
ON INVENTARIO.CODIGOELEMENTO = DEVOLUTIVO.ELEMENTO 
AND INVENTARIO.COMPANIA = DEVOLUTIVO.COMPANIA
WHERE INVENTARIO.COMPANIA = s$compania$s 
AND DEVOLUTIVO.SERIE < (SELECT VALOR FROM PARAMETRO WHERE NOMBRE IN ('CONSECUTIVO INVENTARIO INICIAL') AND COMPANIA = s$compania$s) AND MODULO IN (10)
AND INVENTARIO.IDENTIFICADOR IN ('C') 
ORDER BY INVENTARIO.CODIGOELEMENTO , DEVOLUTIVO.SERIE
