SELECT 
    DEPENDENCIA.NOMBRE   NOMBREDEP,
    SUBSTR(DEVOLUTIVO.ELEMENTO,0,s$digitos$s)  AGRUPACION,
    DEVOLUTIVO.SERIE   SERIEDEVOL,
    TRIM(INVENTARIO.NOMBRELARGO)
    || ' MARCA:'
    || DEVOLUTIVO.MARCA
    || ' -'
    || DEVOLUTIVO.DESCRIPCION
    || CASE WHEN TRIM(SERIEDEVOLUTIVO) IN ('')THEN '' ELSE ' SERIE:' 
    || SERIEDEVOLUTIVO END   NOMBRELARGOS,
    TO_CHAR(DEVOLUTIVO.FECHAENTRADA,'DD/MM/YYYY') FECHAENTRADA,
    TO_CHAR(DEVOLUTIVO.FECHASALIDASERVICIO,'DD/MM/YYYY')  FECHASALIDA
  FROM DEVOLUTIVO
  INNER JOIN INVENTARIO
  ON DEVOLUTIVO.COMPANIA  = INVENTARIO.COMPANIA
  AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
  INNER JOIN DEPENDENCIA
  ON DEVOLUTIVO.COMPANIA     = DEPENDENCIA.COMPANIA
  AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO
  WHERE DEVOLUTIVO.COMPANIA = s$compania$s
  AND DEVOLUTIVO.DEPENDENCIA BETWEEN s$codigoInicial$s AND s$codigoFinal$s
  AND DEVOLUTIVO.FECHAENTRADA BETWEEN s$fechaEntradaInicial$s AND s$fechaEntradaFinal$s
  ORDER BY DEPENDENCIA.NOMBRE,
  DEVOLUTIVO.ELEMENTO 
