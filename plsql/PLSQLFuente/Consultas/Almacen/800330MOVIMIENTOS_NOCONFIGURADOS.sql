MERGE INTO CONSULTAS FIN USING (SELECT '800330MOVIMIENTOS_NOCONFIGURADOS' INFORME ,TO_CLOB(q'[SELECT MOVIMIENTO.TIPOMOVIMIENTO,
   SUBSTR(D_MOVIMIENTO.ELEMENTO,1,PCK_SYSMAN_UTL.FC_PAR(D_MOVIMIENTO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',1,D_MOVIMIENTO.FECHA)) GRUPO,
   TO_NUMBER(TO_CHAR(D_MOVIMIENTO.FECHA,'YYYY')) ANO,
   PCK_SYSMAN_UTL.FC_PAR(D_MOVIMIENTO.COMPANIA,'TIPO COMPROBANTE INTERFASE DIARIA ALMACEN',1,D_MOVIMIENTO.FECHA) TIPO_CPTE,
   CONCAT(s$ano$s,TO_CHAR(D_MOVIMIENTO.FECHA,'MM'))||'00'||TO_CHAR(D_MOVIMIENTO.FECHA,'DD')COMPROBANTE,
   D_MOVIMIENTO.FECHA,
   CASE WHEN 0 NOT IN(0) THEN  NIIF_CUENTADEBITO   ELSE  ALMACENCONTABILIDAD.CUENTADEBITO   END  CUENTADEBITO,
   CASE WHEN 0 NOT IN(0) THEN  NIIF_CUENTACREDITO  ELSE  ALMACENCONTABILIDAD.CUENTACREDITO END   CUENTACREDITO, 
   SUM(CASE WHEN  0  NOT IN(0) THEN NIIF_VALOR_TOTAL ELSE D_MOVIMIENTO.VALORTOTAL  END)    VALOR
  FROM   MOVIMIENTO INNER JOIN D_MOVIMIENTO
      ON MOVIMIENTO.COMPANIA       = D_MOVIMIENTO.COMPANIA
     AND MOVIMIENTO.TIPOMOVIMIENTO = D_MOVIMIENTO.TIPOMOVIMIENTO
     AND MOVIMIENTO.NUMERO         = D_MOVIMIENTO.MOVIMIENTO
  INNER JOIN TIPOMOVIMIENTO 
    ON TIPOMOVIMIENTO.COMPANIA = D_MOVIMIENTO.COMPANIA
   AND TIPOMOVIMIENTO.CODIGO   = D_MOVIMIENTO.TIPOMOVIMIENTO
  LEFT JOIN ALMACENCONTABILIDAD 
    ON D_MOVIMIENTO.COMPANIA                          = ALMACENCONTABILIDAD.COMPANIA
    AND TO_NUMBER(TO_CHAR(D_MOVIMIENTO.FECHA,'YYYY')) = ALMACENCONTABILIDAD.ANO
   AND D_MOVIMIENTO.TIPOMOVIMIENTO                    = ALMACENCONTABILIDAD.TIPOMOVIMIENTO
   AND SUBSTR(D_MOVIMIENTO.ELEMENTO,1,LENGTH(ALMACENCONTABILIDAD.CODIGOELEMENTO)) = ALMACENCONTABILIDAD.CODIGOELEMENTO
  WHERE D_MOVIMIENTO.COMPANIA  = s$compania$s
    AND TRUNC(D_MOVIMIENTO.FECHA) BETWEEN TO_DATE('01/'||s$mesFinal$s||'/'||s$ano$s,'DD/MM/YYYY') AND TO_DATE('s$ultimoDia$s','DD/MM/YYYY')
    AND IND_REG  NOT IN(0)
    AND TO_CHAR(D_MOVIMIENTO.FECHA,'YYYY') = s$ano$s
    AND NOT (TIPOMOVIMIENTO.CONCEPTO IN ('T') AND TIPOMOVIMIENTO.CLASE IN('T'))
    AND (ALMACENCONTABILIDAD.CUENTADEBITO IS NULL OR ALMACENCONTABILIDAD.CUENTADEBITO IS NULL) 
  GROUP BY MOVIMIENTO.TIPOMOVIMIENTO,
           SUBSTR(D_MOVIMIENTO.ELEMENTO,1,PCK_SYSMAN_UTL.FC_PAR(D_MOVIMIENTO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',1,D_MOVIMIENTO.FECHA)),
           TO_NUMBER(TO_CHAR(D_MOVIMIENTO.FECHA,'YYYY')),
          D_MOVIMIENTO.FECHA,
          CASE WHEN 0 NOT IN(0) THEN   NIIF_CUENTADEBITO   ELSE  ALMACENCONTABILIDAD.CUENTADEBITO  END , 
          CASE WHEN 0 NOT IN(0) THEN   NIIF_CUENTACREDITO  ELSE  ALMACENCONTABILIDAD.CUENTACREDITO  END ,
           0,
           PCK_SYSMAN_UTL.FC_PAR(D_MOVIMIENTO.COMPANIA,'TIPO COMPROBANTE INTERFASE DIARIA ALMACEN',1,D_MOVIMIENTO.FECHA)]') CONSULTA, 96 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);