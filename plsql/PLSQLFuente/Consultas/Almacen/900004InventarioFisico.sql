SELECT  DEVOLUTIVO.DEPENDENCIA,
  DEPENDENCIA.NOMBRE  NOMBREDEPENDENCIA,
  DEVOLUTIVO.RESPONSABLE,
  TERCERO.NOMBRE      NOMBRE_RESPONSABLE,
  DEVOLUTIVO.SERIE,
  DEVOLUTIVO.ELEMENTO,
  INVENTARIO.NOMBRELARGO || ' '  || CASE (NVL(DEVOLUTIVO.MARCA , ' ' )) WHEN '' THEN ' '  ELSE 'Marca: ' || DEVOLUTIVO.MARCA END MARCA,
  DEVOLUTIVO.DESCRIPCION  DESCRIPCION,
  (CASE(NVL(V_BASE_TRANSACCIONESALMACEN.ESCRITURA,' '))       WHEN ' ' THEN '' ELSE 'Escritura: ' || V_BASE_TRANSACCIONESALMACEN.ESCRITURA       END || ' ' ||
   CASE(NVL(V_BASE_TRANSACCIONESALMACEN.NUMEROCATASTRAL,' ')) WHEN ' ' THEN '' ELSE 'Catastro: '  || V_BASE_TRANSACCIONESALMACEN.NUMEROCATASTRAL END || ' ' ||
   CASE(NVL(V_BASE_TRANSACCIONESALMACEN.MATINMOBILIARIA,' ')) WHEN ' ' THEN '' ELSE 'Matricula: ' || V_BASE_TRANSACCIONESALMACEN.MATINMOBILIARIA END || ' ' ||
   CASE(NVL(V_BASE_TRANSACCIONESALMACEN.COD_CHIP,' '))        WHEN ' ' THEN '' ELSE 'Chip: '      || V_BASE_TRANSACCIONESALMACEN.COD_CHIP        END || ' ' ||
   CASE (NVL(DEVOLUTIVO.SERIE,'0'))            WHEN 0   THEN '' ELSE 'Serie: '     || DEVOLUTIVO.SERIE             END || ' ' ||
   CASE(NVL(V_BASE_TRANSACCIONESALMACEN.AREA,' '))            WHEN ' ' THEN '' ELSE 'Area: '      || V_BASE_TRANSACCIONESALMACEN.AREA            END 
  )  NOMBRELARGO,
  DEVOLUTIVO.SERIEDEVOLUTIVO,
  DEVOLUTIVO.FECHASALIDASERVICIO  FECHASALIDASERVICIO,
  UNIDAD.NOMBRE                   NOMBRE_UNIDAD,
  DEVOLUTIVO.ESTADO,
  DEVOLUTIVO.VALOR
FROM  DEVOLUTIVO INNER JOIN V_BASE_TRANSACCIONESALMACEN  
          ON V_BASE_TRANSACCIONESALMACEN.COMPANIA = DEVOLUTIVO.COMPANIA
         AND V_BASE_TRANSACCIONESALMACEN.TIPOMOVIMIENTO = DEVOLUTIVO.TIPOMOVIMIENTOI 
         AND V_BASE_TRANSACCIONESALMACEN.ELEMENTO = DEVOLUTIVO.ELEMENTO
         AND V_BASE_TRANSACCIONESALMACEN.SERIE = DEVOLUTIVO.SERIE
  INNER JOIN RESPONSABLE 
         ON DEVOLUTIVO.COMPANIA             = RESPONSABLE.COMPANIA
        AND DEVOLUTIVO.RESPONSABLE          = RESPONSABLE.CEDULA
        AND DEVOLUTIVO.SUCURSAL_RESPONSABLE = RESPONSABLE.SUCURSAL
  INNER JOIN INVENTARIO
          ON DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA 
         AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
  INNER JOIN DEPENDENCIA 
          ON DEVOLUTIVO.COMPANIA    = DEPENDENCIA.COMPANIA 
         AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO
  LEFT JOIN UNIDAD 
         ON INVENTARIO.UNIDAD = UNIDAD.UNIDAD  
  INNER JOIN TERCERO 
     ON RESPONSABLE.COMPANIA = TERCERO.COMPANIA
    AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
    AND RESPONSABLE.CEDULA   = TERCERO.NIT    
WHERE DEVOLUTIVO.COMPANIA =   s$compania$s
  AND DEVOLUTIVO.DEPENDENCIA BETWEEN  s$dependencia$s AND  s$dependenciaFinal$s
  AND DEVOLUTIVO.PLACAANULADA  IN(0)
  AND DEVOLUTIVO.FECHAADQUISICION BETWEEN  s$fechaInicial$s AND  s$fechaFinal$s
ORDER BY DEVOLUTIVO.COMPANIA, DEVOLUTIVO.DEPENDENCIA, DEVOLUTIVO.ELEMENTO, DEVOLUTIVO.SERIE
