SELECT
 MOVIMIENTO.COMPANIA,
 MOVIMIENTO.TIPOMOVIMIENTO,
 CASE WHEN  TRIM(INVENTARIO.NOMBRELARGO) IS NULL THEN '' ELSE
    'Elemento: ' || INVENTARIO.NOMBRELARGO  END || 
  CASE WHEN  TRIM(DEVOLUTIVO.DESCRIPCION) IS NULL THEN '' ELSE
    ' Especificación: '|| DEVOLUTIVO.DESCRIPCION END NOMBRELARGO,
  MOVIMIENTO.NUMERO MOVIMIENTO,
  MOVIMIENTO.FECHA FECHA_MOV,
  MOVIMIENTO.DEPENDENCIA_ORIGEN,
  MOVIMIENTO.DEPENDENCIA_DESTINO,
  TRIM(MOVIMIENTO.DESCRIPCION) DESCRIPCION_MOV,
  D_MOVIMIENTO.ELEMENTO CODIGO,
  SUBSTR(D_MOVIMIENTO.ELEMENTO ,0,3) CODGRUPO,
  DEPENDENCIAORIGEN.NOMBRE NOMBREORI,
  D_MOVIMIENTO.SERIE,
  D_MOVIMIENTO.VALORUNITARIO,
  D_MOVIMIENTO.VALORTOTAL VALORTOTAL,
  DEPENDENCIADESTINO.NOMBRE NOMBREDES,
  INVENTARIO.NOMBRELARGO
  || ' '
  || INVENTARIO.DESCRIPCION DESCRIPCION_INV,
  TIPOMOVIMIENTO.NOMBRE TIPOMOVINOMBRE,
  CASE WHEN  TRIM(DEVOLUTIVO.SERIEDEVOLUTIVO) IS NULL 
       THEN '' 
       ELSE DEVOLUTIVO.SERIEDEVOLUTIVO 
  END  SERIEDEVOLUTIVO,
  CASE WHEN  TRIM(DEVOLUTIVO.MARCA) IS NULL 
       THEN '' 
       ELSE DEVOLUTIVO.MARCA 
  END  MARCA,
  MOVIMIENTO.TIPOMOVIMIENTO
  || MOVIMIENTO.NUMERO GRUPO,
  MOVIMIENTO.RESPONSABLE_ORIGEN CEDULARESPONORIG,
  RESPONSABLE_ORIGEN_NOM.NOMBRE RESPONSABLEORIGEN,
  MOVIMIENTO.RESPONSABLE_DESTINO CEDULARESPONDEST,
  RESPONSABLE_DESTINO_NOM.NOMBRE RESPONSABLEDESTINO
 FROM D_MOVIMIENTO
 INNER JOIN INVENTARIO
 ON D_MOVIMIENTO.COMPANIA  = INVENTARIO.COMPANIA
 AND D_MOVIMIENTO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
 INNER JOIN MOVIMIENTO
 ON D_MOVIMIENTO.COMPANIA        = MOVIMIENTO.COMPANIA
 AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO
 AND D_MOVIMIENTO.MOVIMIENTO     = MOVIMIENTO.NUMERO
 LEFT JOIN DEPENDENCIA DEPENDENCIAORIGEN
 ON DEPENDENCIAORIGEN.COMPANIA = MOVIMIENTO.COMPANIA
 AND DEPENDENCIAORIGEN.CODIGO  = MOVIMIENTO.DEPENDENCIA_ORIGEN
 LEFT JOIN DEPENDENCIA DEPENDENCIADESTINO
 ON DEPENDENCIADESTINO.COMPANIA = MOVIMIENTO.COMPANIA
 AND DEPENDENCIADESTINO.CODIGO  = MOVIMIENTO.DEPENDENCIA_DESTINO
 INNER JOIN TIPOMOVIMIENTO
 ON MOVIMIENTO.COMPANIA        = TIPOMOVIMIENTO.COMPANIA
 AND MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
 LEFT JOIN DEPENDENCIA_RESPONSABLE RESPONSABLE_ORIGEN
 ON RESPONSABLE_ORIGEN.COMPANIA     = MOVIMIENTO.COMPANIA
 AND RESPONSABLE_ORIGEN.DEPENDENCIA = MOVIMIENTO.DEPENDENCIA_ORIGEN
 AND RESPONSABLE_ORIGEN.RESPONSABLE = MOVIMIENTO.RESPONSABLE_ORIGEN
 AND RESPONSABLE_ORIGEN.SUCURSAL    = MOVIMIENTO.SUCURSAL_RESORIGEN
 LEFT JOIN DEPENDENCIA_RESPONSABLE RESPONSABLE_DESTINO
 ON RESPONSABLE_DESTINO.COMPANIA     = MOVIMIENTO.COMPANIA
 AND RESPONSABLE_DESTINO.DEPENDENCIA = MOVIMIENTO.DEPENDENCIA_DESTINO
 AND RESPONSABLE_DESTINO.RESPONSABLE = MOVIMIENTO.RESPONSABLE_DESTINO
 AND RESPONSABLE_DESTINO.SUCURSAL    = MOVIMIENTO.SUCURSAL_RESDESTINO
 LEFT JOIN TERCERO RESPONSABLE_ORIGEN_NOM
 ON RESPONSABLE_ORIGEN_NOM.COMPANIA  = RESPONSABLE_ORIGEN.COMPANIA
 AND RESPONSABLE_ORIGEN_NOM.SUCURSAL = RESPONSABLE_ORIGEN.SUCURSAL
 AND RESPONSABLE_ORIGEN_NOM.NIT      = RESPONSABLE_ORIGEN.RESPONSABLE
 LEFT JOIN TERCERO RESPONSABLE_DESTINO_NOM
 ON RESPONSABLE_DESTINO_NOM.COMPANIA  = RESPONSABLE_DESTINO.COMPANIA
 AND RESPONSABLE_DESTINO_NOM.SUCURSAL = RESPONSABLE_DESTINO.SUCURSAL
 AND RESPONSABLE_DESTINO_NOM.NIT      = RESPONSABLE_DESTINO.RESPONSABLE
 LEFT JOIN DEVOLUTIVO
 ON D_MOVIMIENTO.COMPANIA    = DEVOLUTIVO.COMPANIA
 AND D_MOVIMIENTO.ELEMENTO   = DEVOLUTIVO.ELEMENTO
 AND D_MOVIMIENTO.SERIE      = DEVOLUTIVO.SERIE
 WHERE D_MOVIMIENTO.COMPANIA   = s$compania$s
 AND MOVIMIENTO.TIPOMOVIMIENTO = 's$tipoMovimiento$s'
 AND MOVIMIENTO.NUMERO BETWEEN s$movimientoInicial$s AND s$movimientoFinal$s
 ORDER BY MOVIMIENTO.COMPANIA,
  MOVIMIENTO.TIPOMOVIMIENTO,
  MOVIMIENTO.NUMERO
