SELECT  D_MOVIMIENTO.COMPANIA,
        SUM(D_MOVIMIENTO.VALORTOTAL) SUMADEVALORTOTAL,
        INVENTARIO.NOMBRECORTO,
        MOVIMIENTO.TERCERO,  
        TERCERO.NOMBRE,
	D_MOVIMIENTO.TIPOMOVIMIENTO,
        MAX(MOVIMIENTO.DESCRIPCION) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS ÚLTIMODEDESCRIPCION,
        D_MOVIMIENTO.TIPOMOVIMIENTO||PCK_SYSMAN_UTL.FC_STRZERO(D_MOVIMIENTO.MOVIMIENTO,11) AS TIPOMOVMOVIMIENTO, 
        SUBSTR(D_MOVIMIENTO.ELEMENTO,(CASE WHEN PCK_SYSMAN_UTL.FC_PAR(D_MOVIMIENTO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',PCK_DATOS.FC_MODULOALMACEN,SYSDATE) = ''
                                          THEN '3'
                                          ELSE PCK_SYSMAN_UTL.FC_PAR(D_MOVIMIENTO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',PCK_DATOS.FC_MODULOALMACEN,SYSDATE)
                                          END )) AGRUPACION
FROM D_MOVIMIENTO 
	INNER JOIN V_MOVIMIENTO MOVIMIENTO 
		ON  D_MOVIMIENTO.COMPANIA 	    = MOVIMIENTO.COMPANIA 
		AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO 
		AND D_MOVIMIENTO.MOVIMIENTO     = MOVIMIENTO.NUMERO 
	INNER JOIN TERCERO 
		ON  MOVIMIENTO.COMPANIA = TERCERO.COMPANIA 
		AND MOVIMIENTO.SUCURSAL = TERCERO.SUCURSAL 
		AND MOVIMIENTO.TERCERO  = TERCERO.NIT
 INNER JOIN INVENTARIO
     ON  INVENTARIO.COMPANIA       = D_MOVIMIENTO.COMPANIA
  WHERE D_MOVIMIENTO.COMPANIA     = s$compania$s 
    AND INVENTARIO.CODIGOELEMENTO = SUBSTR(D_MOVIMIENTO.ELEMENTO,0,
         (CASE WHEN PCK_SYSMAN_UTL.FC_PAR(D_MOVIMIENTO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',PCK_DATOS.FC_MODULOALMACEN,SYSDATE) = ''
                      THEN '3'
                      ELSE PCK_SYSMAN_UTL.FC_PAR(D_MOVIMIENTO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',PCK_DATOS.FC_MODULOALMACEN,SYSDATE)
                      END )) 
  AND MOVIMIENTO.CLASE 			= 'E'
  AND INVENTARIO.TIPO       = 'E'
  AND D_MOVIMIENTO.FECHA BETWEEN s$fechaInicial$s
		                         AND s$fechaFinal$s 
	AND D_MOVIMIENTO.IND_REG NOT IN 0 
	AND MOVIMIENTO.CONCEPTO <> ' '  
GROUP BY  D_MOVIMIENTO.COMPANIA,
          D_MOVIMIENTO.TIPOMOVIMIENTO, 
          D_MOVIMIENTO.MOVIMIENTO, 
          MOVIMIENTO.CLASE, 
          INVENTARIO.TIPO, 
          INVENTARIO.NOMBRECORTO, 
          SUBSTR(D_MOVIMIENTO.ELEMENTO,(CASE WHEN PCK_SYSMAN_UTL.FC_PAR(D_MOVIMIENTO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',PCK_DATOS.FC_MODULOALMACEN,SYSDATE) = ''
                                          THEN '3'
                                          ELSE PCK_SYSMAN_UTL.FC_PAR(D_MOVIMIENTO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',PCK_DATOS.FC_MODULOALMACEN,SYSDATE)
                                          END )), 
          MOVIMIENTO.CONCEPTO, 
          MOVIMIENTO.TERCERO, 
          MOVIMIENTO.SUCURSAL, 
          TERCERO.NOMBRE, 
          INVENTARIO.NOMBRELARGO, 
          MOVIMIENTO.DESCRIPCION, 
          D_MOVIMIENTO.TIPOMOVIMIENTO||PCK_SYSMAN_UTL.FC_STRZERO(D_MOVIMIENTO.MOVIMIENTO,11)
