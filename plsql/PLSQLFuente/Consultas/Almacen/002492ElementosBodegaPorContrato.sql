MERGE INTO CONSULTAS FIN USING (SELECT '002492ElementosBodegaPorContrato' INFORME ,TO_CLOB(q'[   SELECT 
   MOVIMIENTO.TIPOMOVASOCIADO || ' - ' || MOVIMIENTO.MOVASOCIADO CONTRATO,
   OC.OBJETOCONTRATO,
   T.NOMBRE SUPERVISOR,
        MOVIMIENTO.CODIGOPROYECTO,
        MOVIMIENTO.TIPOMOVIMIENTO || ' - ' || MOVIMIENTO.NUMERO INGRESO,
        TO_CHAR(MOVIMIENTO.FECHA,'DD/MM/YYYY') FECHA,
        D_MOVIMIENTO.CANTIDAD,
        D_MOVIMIENTO.ELEMENTO,
        INVENTARIO.NOMBRELARGO, 
        UNIDAD.NOMBRE UNIDAD,
        (SUM( D_MOVIMIENTO.CANTIDAD) - NVL(SALIDAS.CANT_SALIDA,0)) SALDO
FROM MOVIMIENTO INNER JOIN D_MOVIMIENTO
ON MOVIMIENTO.COMPANIA=D_MOVIMIENTO.COMPANIA
AND  MOVIMIENTO.TIPOMOVIMIENTO=D_MOVIMIENTO.TIPOMOVIMIENTO
AND  MOVIMIENTO.NUMERO=D_MOVIMIENTO.MOVIMIENTO
INNER JOIN INVENTARIO 
ON D_MOVIMIENTO.COMPANIA=INVENTARIO.COMPANIA
AND  D_MOVIMIENTO.ELEMENTO=INVENTARIO.CODIGOELEMENTO 
INNER JOIN TIPOMOVIMIENTO
ON TIPOMOVIMIENTO.COMPANIA=D_MOVIMIENTO.COMPANIA
AND  TIPOMOVIMIENTO.CODIGO=D_MOVIMIENTO.TIPOMOVIMIENTO
INNER JOIN UNIDAD
ON INVENTARIO.UNIDAD=UNIDAD.UNIDAD

INNER JOIN ORDENDECOMPRA OC
 ON MOVIMIENTO.COMPANIA = OC.COMPANIA
AND MOVIMIENTO.TIPOMOVASOCIADO = OC.CLASEORDEN
AND MOVIMIENTO.MOVASOCIADO = OC.NUMERO
LEFT JOIN SUPERVISORES S
 ON OC.COMPANIA = S.COMPANIA
AND OC.CLASEORDEN = S.CLASEORDEN
AND OC.NUMERO = S.NUMEROCONTRATO
LEFT JOIN TERCERO T
 ON S.COMPANIA = T.COMPANIA 
AND S.CEDULA = T.NIT
AND S.SUCURSAL = T.SUCURSAL
 LEFT JOIN (SELECT D_MOVIMIENTO.COMPANIA, 
         D_MOVIMIENTO.TIPOMOVIMIENTO_AFECT,
         D_MOVIMIENTO.MOVIMIENTO_AFECT,
         D_MOVIMIENTO.CODIGO_AFECT,
         D_MOVIMIENTO.ELEMENTO,
         SUM(D_MOVIMIENTO.CANTIDAD) CANT_SALIDA
         FROM  D_MOVIMIENTO 
         INNER JOIN TIPOMOVIMIENTO
ON TIPOMOVIMIENTO.COMPANIA=D_MOVIMIENTO.COMPANIA
AND  TIPOMOVIMIENTO.CODIGO=D_MOVIMIENTO.TIPOMOVIMIENTO
WHERE TIPOMOVIMIENTO.CLASE IN ('S')
AND TIPOMOVIMIENTO.TIPOELEMENTO IN ('C')
GROUP BY D_MOVIMIENTO.COMPANIA, 
         D_MOVIMIENTO.TIPOMOVIMIENTO_AFECT,
         D_MOVIMIENTO.MOVIMIENTO_AFECT,
         D_MOVIMIENTO.CODIGO_AFECT,
         D_MOVIMIENTO.ELEMENTO ) SALIDAS
 ON D_MOVIMIENTO.COMPANIA = SALIDAS.COMPANIA
AND D_MOVIMIENTO.TIPOMOVIMIENTO = SALIDAS.TIPOMOVIMIENTO_AFECT
AND D_MOVIMIENTO.MOVIMIENTO = SALIDAS.MOVIMIENTO_AFECT
AND D_MOVIMIENTO.CODIGO = SALIDAS.CODIGO_AFECT
AND D_MOVIMIENTO.ELEMENTO = SALIDAS.ELEMENTO
WHERE MOVIMIENTO.COMPANIA = s$compania$s
AND MOVIMIENTO.TIPOMOVASOCIADO BETWEEN 's$contratoIni$s' AND 's$contratoFin$s'
AND MOVIMIENTO.CODIGOPROYECTO BETWEEN 's$proyectoIni$s' AND 's$proyectoFin$s'
AND INVENTARIO.TIPO IN('C')
AND TIPOMOVIMIENTO.CLASE IN('E')
AND D_MOVIMIENTO.FECHA <= s$fechaCorte$s
GROUP BY MOVIMIENTO.TIPOMOVASOCIADO || ' - ' || MOVIMIENTO.MOVASOCIADO, OC.OBJETOCONTRATO, T.NOMBRE, MOVIMIENTO.CODIGOPROYECTO, MOVIMIENTO.TIPOMOVIMIENTO || ' - ' || MOVIMIENTO.NUMERO, 
TO_CHAR(MOVIMIENTO.FECHA,'DD/MM/YYYY'), D_MOVIMIENTO.CANTIDAD, D_MOVIMIENTO.ELEMENTO, INVENTARIO.NOMBRELARGO, UNIDAD.NOMBRE, SALIDAS.CANT_SALIDA,  MOVIM]') || TO_CLOB(q'[IENTO.MOVASOCIADO
ORDER BY  MOVIMIENTO.MOVASOCIADO]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);