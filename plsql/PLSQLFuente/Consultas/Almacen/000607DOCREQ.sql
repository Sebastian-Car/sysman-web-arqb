SELECT  FECHA FECHA_SOLICITUD, TE.NOMBRE TIPO_EVENTO, TD.DESCRIPCION, 
        RESPUESTA, RP.NOMBRE DOCUMENTO, RP.DESCRIPCION DESC_DOCUMENTO
FROM    TRANSACCION_DEVOLUTIVO TD
        INNER JOIN TIPO_EVENTO TE
        ON  TD.COMPANIA    = TE.COMPANIA
        AND TD.TIPO_EVENTO = TE.CODIGO
        INNER JOIN TRANSACCION_REQUISITOS TR
        ON  TD.COMPANIA    = TR.COMPANIA
        AND TD.TIPO        = TR.TIPO_TRANSACCION
        AND TD.CODIGO      = TR.CODIGO_TRANSACCION
        INNER JOIN REQUISITOS_PRESTAMO RP
        ON  TR.COMPANIA    = RP.COMPANIA
        AND TR.REQUISITO   = RP.CODIGO
WHERE   TD.COMPANIA = s$compania$s
AND     TD.TIPO = 's$tipo$s'
AND     TD.CODIGO = s$codigo$s
