SELECT 	   
       V_MOVIMIENTO_INFORME.COMPANIA,
	     V_MOVIMIENTO_INFORME.MOVIMIENTO,
       V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO_NOM,
       V_MOVIMIENTO_INFORME.DEPENDENCIA_DESTINO_NOM,
       NVL(V_MOVIMIENTO_INFORME.VALORTOTAL_MOV,0) VALORTOTAL,
       NVL(V_MOVIMIENTO_INFORME.VALORUNITARIO,0) VALORUNITARIO,
       NVL(V_MOVIMIENTO_INFORME.CANTIDAD,0) CANTIDAD,
       V_MOVIMIENTO_INFORME.ELEMENTO,
       V_MOVIMIENTO_INFORME.SERIE,
       V_MOVIMIENTO_INFORME.NOMBRELARGO,
       V_MOVIMIENTO_INFORME.UNIDAD_NOM UNIDAD,
       NVL(V_MOVIMIENTO_INFORME.CANTIDAD,0) * NVL(V_MOVIMIENTO_INFORME.VALORTOTAL_MOV,0) TOTALCOLUMNADETALLE,
       V_MOVIMIENTO_INFORME.FECHA_MOV FECHA,
       NVL(V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO_CARGO,'N/A') CARGO,
       V_MOVIMIENTO_INFORME.NOMBRELARGO || ' ' || NVL(V_MOVIMIENTO_INFORME.ESPECIFICACION,' ') NOMBREELEMENTO,
       V_MOVIMIENTO_INFORME.DESCRIPCION_MOV DESCRIPCION,
       V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO_NOM,
       V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO
FROM V_MOVIMIENTO_INFORME
WHERE V_MOVIMIENTO_INFORME.COMPANIA       = s$compania$s  
	AND V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO = 's$tipoMovimiento$s'
	AND V_MOVIMIENTO_INFORME.MOVIMIENTO BETWEEN s$movimientoInicial$s AND s$movimientoFinal$s
