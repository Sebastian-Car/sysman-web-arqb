SELECT 	TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY') VIGENCIA,
       	COUNT(CASE WHEN COMPROBANTE_PPTAL.DESTINO='I' 
               	   THEN 'INVERSION' 
                   END) INVERSION ,
        COUNT(CASE WHEN COMPROBANTE_PPTAL.DESTINO='F' 
                   THEN 'FUNCIONAMIENTO' 
                   END) FUNCIONAMIENTO
FROM (ORDENDECOMPRA
     	INNER JOIN ORDENDECOMPRAPPTO
          	ON  ORDENDECOMPRA.COMPANIA    = ORDENDECOMPRAPPTO.COMPANIA 
          	AND ORDENDECOMPRA.CLASEORDEN  = ORDENDECOMPRAPPTO.CLASEORDEN 
          	AND ORDENDECOMPRA.NUMERO      = ORDENDECOMPRAPPTO.NUMERO) 
     			INNER JOIN COMPROBANTE_PPTAL
          			ON  ORDENDECOMPRAPPTO.COMPANIA   = COMPROBANTE_PPTAL.COMPANIA
          			AND ORDENDECOMPRAPPTO.TIPOPPTO   = COMPROBANTE_PPTAL.TIPO
          			AND ORDENDECOMPRAPPTO.NUMEROPPTO = COMPROBANTE_PPTAL.NUMERO
          			AND ORDENDECOMPRAPPTO.ANOPPTO    = COMPROBANTE_PPTAL.ANO
WHERE ORDENDECOMPRA.COMPANIA       = 's$compania$s'
  AND ORDENDECOMPRA.DEPENDENCIA    = s$dependencia$s
 AND TO_NUMBER(TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY'))
          BETWEEN  s$anioInicial$s  AND s$anioFinal$s
GROUP BY TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY')
