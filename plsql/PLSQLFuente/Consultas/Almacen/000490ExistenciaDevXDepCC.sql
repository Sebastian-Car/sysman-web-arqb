SELECT  
  DEVOLUTIVO.COMPANIA, 
  DEVOLUTIVO.DEPENDENCIA, 
  DEVOLUTIVO.ELEMENTO, 
  DEVOLUTIVO.REFERENCIA, 
  DEVOLUTIVO.SERIE, 
  TO_CHAR(DEVOLUTIVO.FECHAADQUISICION,'DD/MM/YYYY') FECHAADQUISICION, 
  DEVOLUTIVO.ESTADO, 
  DEVOLUTIVO.SERIEDEVOLUTIVO, 
  DEVOLUTIVO.RESPONSABLE, 
  DEVOLUTIVO.VALOR, 
  INVENTARIO.CODIGOELEMENTO, 
  DEPENDENCIA.NOMBRE DEPENDENCIANOMBRE, 
  TERCERO.NOMBRE RESPONSABLENOMBRE, 
  INVENTARIO.UNIDAD, 
  DEVOLUTIVO.VALOR TVALOR, 
  DEVOLUTIVO.DESCRIPCION, 
  INVENTARIO.NOMBRELARGO||' '|| CASE WHEN NVL(DEVOLUTIVO.MARCA,' ')=' ' THEN '' ELSE '- '||DEVOLUTIVO.MARCA||' '||DEVOLUTIVO.DESCRIPCION END NOMBRELARGO, 
  SUBSTR(DEVOLUTIVO.ELEMENTO,0,3) AGRUPACION 
FROM DEVOLUTIVO  
  LEFT JOIN RESPONSABLE  
    ON  DEVOLUTIVO.RESPONSABLE = RESPONSABLE.CEDULA 
    AND DEVOLUTIVO.COMPANIA = RESPONSABLE.COMPANIA 
  INNER JOIN TERCERO 
    ON  RESPONSABLE.COMPANIA = TERCERO.COMPANIA 
    AND RESPONSABLE.CEDULA = TERCERO.NIT 
    AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL 
  LEFT JOIN INVENTARIO  
    ON  DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA 
    AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO 
  LEFT JOIN DEPENDENCIA  
    ON  DEVOLUTIVO.COMPANIA = DEPENDENCIA.COMPANIA  
    AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO 
WHERE INVENTARIO.COMPANIA=s$compania$s   
  AND DEVOLUTIVO.DEPENDENCIA BETWEEN 's$dependenciaDesde$s' AND 's$dependenciaHasta$s' 
  AND DEVOLUTIVO.ELEMENTO BETWEEN 's$elementoDesde$s' AND 's$elementoHasta$s' 
  AND DEVOLUTIVO.PLACAANULADA=0 
ORDER BY 
  DEVOLUTIVO.DEPENDENCIA, 
  DEVOLUTIVO.ELEMENTO, 
  DEVOLUTIVO.SERIE