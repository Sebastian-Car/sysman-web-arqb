WITH C_HKARDEXENTCONINSER AS (
                    SELECT 'E' AS CLASE, 
                            DEPENDENCIA.NOMBRE AS DEPENDENCIA, 
                            TERCERO.NOMBRE AS RESPONSABLE, 
                            ELEMENTOBODEGA.ELEMENTO, 
                            ELEMENTOBODEGA.FECHAENTRADA, 
                            ELEMENTOBODEGA.HORAENTRADA, 
                            INVENTARIO.NOMBRELARGO, 
                            INVENTARIO.UNIDAD, 
                            ELEMENTOBODEGA.CANTIDAD ENTRADAS, 
                            ELEMENTOBODEGA.VALORTOTAL VRLENTRADAS, 
                            ELEMENTOBODEGA.TIPOMOVIMIENTO, 
                            ELEMENTOBODEGA.MOVIMIENTO, 
                            ELEMENTOBODEGA.ESTADO, 
                            INVENTARIO.TIPO, 
                            ELEMENTOBODEGA.COMPANIA,
                            ELEMENTOBODEGA.VALORUNITARIO, 
                            ELEMENTOBODEGA.SALDOBODEGA, 
                            ELEMENTOBODEGA.VALORSALDOBODEGA
            FROM ELEMENTOBODEGA 
              INNER JOIN V_MOVIMIENTO
               ON ELEMENTOBODEGA.COMPANIA        = V_MOVIMIENTO.COMPANIA 
               AND ELEMENTOBODEGA.TIPOMOVIMIENTO = V_MOVIMIENTO.TIPOMOVIMIENTO 
               AND ELEMENTOBODEGA.MOVIMIENTO     = V_MOVIMIENTO.NUMERO
              LEFT JOIN DEPENDENCIA 
                ON V_MOVIMIENTO.COMPANIA             = DEPENDENCIA.COMPANIA 
                AND V_MOVIMIENTO.DEPENDENCIA_DESTINO = DEPENDENCIA.CODIGO 
              LEFT JOIN TERCERO 
                ON V_MOVIMIENTO.COMPANIA  = TERCERO.COMPANIA 
                AND V_MOVIMIENTO.TERCERO  = TERCERO.NIT 
                AND V_MOVIMIENTO.SUCURSAL = TERCERO.SUCURSAL
             LEFT JOIN INVENTARIO
               ON ELEMENTOBODEGA.COMPANIA  = INVENTARIO.COMPANIA 
               AND ELEMENTOBODEGA.ELEMENTO = INVENTARIO.CODIGOELEMENTO
            WHERE ELEMENTOBODEGA.COMPANIA = s$compania$s 
              AND ELEMENTOBODEGA.ELEMENTO BETWEEN s$elementoInicial$s AND s$elementoFinal$s 
              AND ELEMENTOBODEGA.FECHAENTRADA BETWEEN s$fechaInicial$s AND s$fechaFinal$s  
              AND INVENTARIO.TIPO = s$tipo$s
              ),
  C_HKARDEXSALCONINSER AS (
            SELECT  
                TIPOMOVIMIENTO.COMPANIA, 
                TIPOMOVIMIENTO.CONCEPTO, 'S' AS CLASE, 
                TIPOMOVIMIENTO.CODIGO AS TIPOMOVIMIENTO, 
                D_MOVIMIENTO.DESCARGO AS MOVIMIENTO, 
                D_MOVIMIENTO.ELEMENTO AS ELEMENTO1, 
                D_MOVIMIENTO.CANTIDAD  SALIDAS, 
                D_MOVIMIENTO.FECHA, 
                D_MOVIMIENTO.HORA,
                DEPENDENCIA.NOMBRE AS DEPENDENCIA1, 
                TERCERO.NOMBRE AS RESPONSABLE1, 
                D_MOVIMIENTO.VALORTOTAL AS VLRSALIDAS, 
                D_MOVIMIENTO.SALDOBODEGA, 
                INVENTARIO.NOMBRELARGO NOMBRELARGO1, 
                INVENTARIO.UNIDAD UNIDAD1, 
                D_MOVIMIENTO.VALORUNITARIO VALORUNITARIO1, 
                D_MOVIMIENTO.SALDOBODEGA AS SALDOBODEGA1, 
                D_MOVIMIENTO.VALORSALDOBODEGA AS VALORSALDOBODEGA1
        FROM TIPOMOVIMIENTO 
          INNER JOIN D_MOVIMIENTO 
            ON TIPOMOVIMIENTO.COMPANIA = D_MOVIMIENTO.COMPANIA 
            AND TIPOMOVIMIENTO.CODIGO  = D_MOVIMIENTO.CLASEDESCARGO 
          LEFT JOIN V_DESCARGOBODEGA 
            ON D_MOVIMIENTO.COMPANIA        = V_DESCARGOBODEGA.COMPANIA 
            AND D_MOVIMIENTO.TIPOMOVIMIENTO = V_DESCARGOBODEGA.TIPOMOVIMIENTO 
            AND D_MOVIMIENTO.DESCARGO       = V_DESCARGOBODEGA.NUMERO 
          LEFT JOIN DEPENDENCIA 
            ON V_DESCARGOBODEGA.COMPANIA             = DEPENDENCIA.COMPANIA 
            AND V_DESCARGOBODEGA.DEPENDENCIA_DESTINO = DEPENDENCIA.CODIGO 
          LEFT JOIN TERCERO 
            ON V_DESCARGOBODEGA.COMPANIA             = TERCERO.COMPANIA 
            AND V_DESCARGOBODEGA.RESPONSABLE_DESTINO = TERCERO.NIT
            AND V_DESCARGOBODEGA.SUCURSAL            = TERCERO.SUCURSAL
          LEFT JOIN INVENTARIO 
            ON D_MOVIMIENTO.COMPANIA      = INVENTARIO.COMPANIA 
            AND D_MOVIMIENTO.ELEMENTO     = INVENTARIO.CODIGOELEMENTO
        WHERE TIPOMOVIMIENTO.COMPANIA     = s$compania$s
          AND TIPOMOVIMIENTO.CONCEPTO     ='DT' 
          AND D_MOVIMIENTO.FECHA BETWEEN  s$fechaInicial$s AND s$fechaFinal$s
          AND TIPOMOVIMIENTO.TIPOELEMENTO = s$tipo$s
          AND TIPOMOVIMIENTO.CLASE        ='D' 
          AND D_MOVIMIENTO.IND_REG NOT IN (0)),
SALDOSANTERIORES AS ( SELECT COMPANIA,
                               TIPOMOVIMIENTO,
                               MOVIMIENTO,
                               ELEMENTO,
                               FECHA,
                               HORA,
                               SALDOKARDEX AS SALDOANTERIOR,
                               VALORUNITARIO AS VLRUNITARIOANT,
                               SALDOKARDEX * VALORUNITARIO TOTALANTERIOR
                        FROM  V_ULTIMOMOVIMIENTO_ALM_INI
                        WHERE V_ULTIMOMOVIMIENTO_ALM_INI.COMPANIA = s$compania$s
                          AND V_ULTIMOMOVIMIENTO_ALM_INI.ELEMENTO  BETWEEN s$elementoInicial$s AND s$elementoFinal$s 
          ),
           TOTALES AS (SELECT   LPAD(ELEMENTO,3) AS AGRUPACIONTOTALES,
                        SUM(CANTIDAD) AS CANTIDADGRUPO, 
                        SUM(VALORUNITARIO) AS VALORUNITARIO, 
                        SUM(VALORTOTAL) AS VALORTOTALGRUPO 
                FROM ELEMENTOBODEGA 
                WHERE COMPANIA          = s$compania$s
                  AND LPAD(ELEMENTO,3) BETWEEN LPAD(s$elementoInicial$s,3) AND LPAD(s$elementoFinal$s,3)
                  AND FECHAENTRADA     <= s$fechaFinal$s  
                  AND FECHACANCELACION <= s$fechaFinal$s
                  GROUP BY LPAD(ELEMENTO,3)
)
SELECT NVL(C_HKARDEXENTCONINSER.ENTRADAS,0)ENTRADAS, 
       NVL(C_HKARDEXSALCONINSER.SALIDAS,0)SALIDAS, 
       NVL(C_HKARDEXENTCONINSER.VRLENTRADAS,0)VlRENTRADAS, 
       NVL(C_HKARDEXSALCONINSER.VLRSALIDAS,0)VLRSALIDAS, 
       CASE WHEN C_HKARDEXENTCONINSER.RESPONSABLE    IS NULL THEN C_HKARDEXSALCONINSER.RESPONSABLE1 ELSE C_HKARDEXENTCONINSER.RESPONSABLE END  AS TERCERO, 
       CASE WHEN C_HKARDEXENTCONINSER.DEPENDENCIA    IS NULL THEN C_HKARDEXSALCONINSER.DEPENDENCIA1 ELSE C_HKARDEXENTCONINSER.DEPENDENCIA END  AS DEPENDENCIA1,
       CASE WHEN C_HKARDEXENTCONINSER.FECHAENTRADA   IS NULL THEN C_HKARDEXSALCONINSER.FECHA ELSE C_HKARDEXENTCONINSER.FECHAENTRADA END  AS FECHA, 
       CASE WHEN C_HKARDEXENTCONINSER.HORAENTRADA    IS NULL THEN C_HKARDEXSALCONINSER.HORA ELSE C_HKARDEXENTCONINSER.HORAENTRADA END AS HORA, 
       CASE WHEN C_HKARDEXENTCONINSER.MOVIMIENTO     IS NULL THEN C_HKARDEXSALCONINSER.MOVIMIENTO ELSE C_HKARDEXENTCONINSER.MOVIMIENTO END  AS MOVIMIENTO,
       CASE WHEN C_HKARDEXENTCONINSER.ELEMENTO       IS NULL THEN C_HKARDEXSALCONINSER.ELEMENTO1 ELSE C_HKARDEXENTCONINSER.ELEMENTO END AS COD_ELEMENTO,
       CASE WHEN C_HKARDEXENTCONINSER.NOMBRELARGO    IS NULL THEN C_HKARDEXSALCONINSER.NOMBRELARGO1 ELSE C_HKARDEXENTCONINSER.NOMBRELARGO END AS NOMBRELARGO, 
       CASE WHEN C_HKARDEXENTCONINSER.ENTRADAS <> 0   THEN C_HKARDEXENTCONINSER.ENTRADAS ELSE 0 END  AS SALDOKARDEX, 
       CASE WHEN C_HKARDEXENTCONINSER.ESTADO         IS NULL THEN 'M' ELSE C_HKARDEXENTCONINSER.ESTADO END  AS ESTADO, 
       CASE WHEN C_HKARDEXENTCONINSER.UNIDAD         IS NULL THEN C_HKARDEXSALCONINSER.UNIDAD1 ELSE C_HKARDEXENTCONINSER.UNIDAD END AS UNIDAD, 
       CASE WHEN C_HKARDEXENTCONINSER.TIPOMOVIMIENTO IS NULL THEN C_HKARDEXSALCONINSER.TIPOMOVIMIENTO ELSE C_HKARDEXENTCONINSER.TIPOMOVIMIENTO END AS TIPOMOV, 
       CASE WHEN C_HKARDEXENTCONINSER.VALORUNITARIO  IS NULL THEN C_HKARDEXSALCONINSER.VALORUNITARIO1 ELSE C_HKARDEXENTCONINSER.VALORUNITARIO END  AS VLRUNITARIO, 
       CASE WHEN C_HKARDEXENTCONINSER.SALDOBODEGA    IS NULL THEN C_HKARDEXSALCONINSER.SALDOBODEGA ELSE C_HKARDEXENTCONINSER.SALDOBODEGA END  AS SALDOBODEGA1,
       CASE WHEN C_HKARDEXENTCONINSER.VALORSALDOBODEGA IS NULL THEN C_HKARDEXSALCONINSER.VALORSALDOBODEGA1 ELSE C_HKARDEXENTCONINSER.VALORSALDOBODEGA END  AS VALORSALDOBODEGA1,
       LPAD(INVENTARIO.CODIGOELEMENTO,3) AS AGRUPACION, 
       LPAD(INVENTARIO.CODIGOELEMENTO,3) || INVENTARIO.NOMBRELARGO AS GRUPO,
       NVL(SALDOSANTERIORES.SALDOANTERIOR,0) SALDOANTERIOR,
       NVL(SALDOSANTERIORES.VLRUNITARIOANT,0)VLRUNITARIOANT,
       NVL(SALDOSANTERIORES.TOTALANTERIOR,0)TOTALANTERIOR,
       TOTALES.CANTIDADGRUPO,
       TOTALES.VALORTOTALGRUPO,
       PCK_GENERALES.FC_NOMBREINVENTARIO(LPAD(INVENTARIO.CODIGOELEMENTO,s$parametro$s) , s$compania$s , NULL) AS NOMBREINVENTARIO
FROM INVENTARIO 
  LEFT JOIN C_HKARDEXENTCONINSER 
    ON INVENTARIO.COMPANIA        = C_HKARDEXENTCONINSER.COMPANIA 
    AND INVENTARIO.CODIGOELEMENTO = C_HKARDEXENTCONINSER.ELEMENTO 
  LEFT JOIN C_HKARDEXSALCONINSER 
    ON INVENTARIO.COMPANIA        = C_HKARDEXSALCONINSER.COMPANIA  
    AND INVENTARIO.CODIGOELEMENTO = C_HKARDEXSALCONINSER.ELEMENTO1 
  LEFT JOIN  SALDOSANTERIORES
    ON INVENTARIO.COMPANIA        = SALDOSANTERIORES.COMPANIA 
    AND INVENTARIO.CODIGOELEMENTO = SALDOSANTERIORES.ELEMENTO 
  LEFT JOIN TOTALES ON   LPAD(INVENTARIO.CODIGOELEMENTO,3) = TOTALES.AGRUPACIONTOTALES  
WHERE INVENTARIO.COMPANIA = s$compania$s 
      AND CASE WHEN C_HKARDEXENTCONINSER.MOVIMIENTO IS NULL THEN C_HKARDEXSALCONINSER.MOVIMIENTO ELSE C_HKARDEXENTCONINSER.MOVIMIENTO END IS NOT NULL 
      AND CASE WHEN C_HKARDEXENTCONINSER.ELEMENTO IS NULL  THEN C_HKARDEXSALCONINSER.ELEMENTO1 ELSE C_HKARDEXENTCONINSER.ELEMENTO END
      BETWEEN s$elementoInicial$s AND s$elementoFinal$s 
ORDER BY  CASE WHEN C_HKARDEXENTCONINSER.FECHAENTRADA IS NULL THEN C_HKARDEXSALCONINSER.FECHA ELSE C_HKARDEXENTCONINSER.FECHAENTRADA END , 
          CASE WHEN C_HKARDEXENTCONINSER.HORAENTRADA IS NULL THEN C_HKARDEXSALCONINSER.HORA ELSE C_HKARDEXENTCONINSER.HORAENTRADA END
