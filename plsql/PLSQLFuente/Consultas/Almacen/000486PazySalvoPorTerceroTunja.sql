SELECT DEPENDENCIA.NOMBRE NOMBRE_DEPENDENCIA, 
TERCERO.NOMBRE NOMBRE_RESPONSABLE, 
RESPONSABLE.CEDULA, 
RESPONSABLE.CARGO, 
DEPENDENCIA_RESPONSABLE.RESPONSABLE,
NVL(PCK_SYSMAN_UTL.FC_PAR(DEPENDENCIA_RESPONSABLE.COMPANIA, 'ENCABEZADO PAZ Y SALVO', PCK_DATOS.FC_MODULOALMACEN, SYSDATE), ' ') ENCABEZADO,
NVL(PCK_SYSMAN_UTL.FC_PAR(DEPENDENCIA_RESPONSABLE.COMPANIA, 'FIRMA DIRECTOR PAZ Y SALVO', PCK_DATOS.FC_MODULOALMACEN, SYSDATE), ' ') FIRMA_DIRECTOR,
NVL(PCK_SYSMAN_UTL.FC_PAR(DEPENDENCIA_RESPONSABLE.COMPANIA, 'FIRMA TECNICO PAZ Y SALVO', PCK_DATOS.FC_MODULOALMACEN, SYSDATE),' ') FIRMA_TECNICO,
NVL(PCK_SYSMAN_UTL.FC_PAR(DEPENDENCIA_RESPONSABLE.COMPANIA, 'PIE DE PAGINA FORMATO CALIDAD TRASLADOS TUNJA', PCK_DATOS.FC_MODULOALMACEN, SYSDATE), ' ') PIE,
NVL(PCK_SYSMAN_UTL.FC_PAR(DEPENDENCIA_RESPONSABLE.COMPANIA, 'CODIGO FORMATO CALIDAD TALENTO HUMANO TUNJA', PCK_DATOS.FC_MODULOALMACEN, SYSDATE),' ') CODIGO,
NVL(PCK_SYSMAN_UTL.FC_PAR(DEPENDENCIA_RESPONSABLE.COMPANIA, 'VERSION FORMATO CALIDAD TALENTO HUMANO TUNJA', PCK_DATOS.FC_MODULOALMACEN, SYSDATE),' ') VERSION,
NVL(PCK_SYSMAN_UTL.FC_PAR(DEPENDENCIA_RESPONSABLE.COMPANIA, 'FECHA FORMATO CALIDAD TALENTO HUMANO TUNJA', PCK_DATOS.FC_MODULOALMACEN, SYSDATE),' ') FECHA
FROM DEPENDENCIA_RESPONSABLE 
  LEFT JOIN DEPENDENCIA ON DEPENDENCIA_RESPONSABLE.COMPANIA = DEPENDENCIA.COMPANIA
    AND DEPENDENCIA_RESPONSABLE.DEPENDENCIA = DEPENDENCIA.CODIGO
  LEFT JOIN RESPONSABLE ON DEPENDENCIA_RESPONSABLE.COMPANIA = RESPONSABLE.COMPANIA
    AND DEPENDENCIA_RESPONSABLE.RESPONSABLE = RESPONSABLE.CEDULA
    AND DEPENDENCIA_RESPONSABLE.SUCURSAL = RESPONSABLE.SUCURSAL
  INNER JOIN TERCERO ON RESPONSABLE.COMPANIA = TERCERO.COMPANIA
    AND RESPONSABLE.CEDULA = TERCERO.NIT
    AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
WHERE DEPENDENCIA_RESPONSABLE.COMPANIA = s$compania$s
  AND DEPENDENCIA_RESPONSABLE.RESPONSABLE='s$responsable$s'
