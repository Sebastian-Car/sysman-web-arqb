SELECT TIPOMOVIMIENTO.CODIGO,
 TIPOMOVIMIENTO.NOMBRE NOMBRETIPO,
 CASE TIPOMOVIMIENTO.INVENTARIOINICIAL WHEN 0 THEN ' ' ELSE 'X' END INVENTARIOINICIAL,
 NVL(TIPOMOVIMIENTO.TIPOFORMATO,' ') TIPOFORMATO,
 CLASE.NOMBRE CLASE,
 TIPOELEMENTO.NOMBRE TIPOELEMENTO,
 DOCASOCIADO.NOMBRE CLASEDOCASOCIADO 
FROM TIPOMOVIMIENTO 
INNER JOIN CLASE 
ON TIPOMOVIMIENTO.CLASE = CLASE.CODIGO 
INNER JOIN TIPOELEMENTO 
ON TIPOMOVIMIENTO.TIPOELEMENTO = TIPOELEMENTO.CODIGO
INNER JOIN DOCASOCIADO
ON TIPOMOVIMIENTO.CLASEDOCASOCIADO = DOCASOCIADO.CODIGO
WHERE TIPOMOVIMIENTO.COMPANIA=s$compania$s
ORDER BY TIPOMOVIMIENTO.COMPANIA,
 TIPOMOVIMIENTO.CODIGO
