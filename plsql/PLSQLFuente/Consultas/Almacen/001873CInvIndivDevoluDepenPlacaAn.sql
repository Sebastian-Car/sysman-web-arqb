SELECT
    DEVOLUTIVO.COMPANIA,
    DEVOLUTIVO.ELEMENTO,
    DEVOLUTIVO.SERIE,
    ESTADODEVOLUTIVO.DESCRIPCION ESTADO,
    DEVOLUTIVO.SERIEDEVOLUTIVO,
    DEVOLUTIVO.DEPENDENCIA,
    DEVOLUTIVO.RESPONSABLE,
    DEVOLUTIVO.VALOR,
    INVENTARIO.CODIGOELEMENTO,
    DEPENDENCIA.NOMBRE,
    DEPENDENCIA.CODIGO,
    NVL(TERCERO.NOMBRE1,'')
    || ' '
    || NVL(TERCERO.NOMBRE2,'')
    || ' '
    || NVL(TERCERO.APELLIDO1,'')
    || ' '
    || NVL(TERCERO.APELLIDO2,'') NOMBRECOMPLETO,
    INVENTARIO.UNIDAD,
    DEVOLUTIVO.VALOR TVALOR,
    UNIDAD.NOMBRE,
    DEVOLUTIVO.DESCRIPCION, 
    INVENTARIO.NOMBRELARGO || ' ' || 
       CASE WHEN NVL(DEVOLUTIVO.MARCA,' ') = ' ' 
        THEN '' 
        ELSE 'Marca: '|| DEVOLUTIVO.MARCA  
        END
    || ' ' || DEVOLUTIVO.DESCRIPCION NOMBRELARGO,
    SUBSTR(DEVOLUTIVO.ELEMENTO,0,3 ) AGRUPACION,
    I.NOMBRELARGO NGRUPO,
    DEVOLUTIVO.FECHAENTRADA,
    INVENTARIO.UNIDAD,
    DEVOLUTIVO.SERIEANTERIOR
FROM DEVOLUTIVO 
    LEFT JOIN RESPONSABLE 
        ON  DEVOLUTIVO.COMPANIA = RESPONSABLE.COMPANIA
        AND   DEVOLUTIVO.RESPONSABLE = RESPONSABLE.CEDULA         
    INNER JOIN TERCERO 
        ON RESPONSABLE.COMPANIA = TERCERO.COMPANIA
        AND RESPONSABLE.CEDULA = TERCERO.NIT
        AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
    LEFT JOIN INVENTARIO 
        ON  DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA 
        AND  DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO  
    LEFT JOIN INVENTARIO I 
        ON  DEVOLUTIVO.COMPANIA = I.COMPANIA 
        AND  SUBSTR (DEVOLUTIVO.ELEMENTO,0,3) = I.CODIGOELEMENTO 
    LEFT JOIN DEPENDENCIA 
        ON  DEVOLUTIVO.COMPANIA = DEPENDENCIA.COMPANIA 
        AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO 
    LEFT JOIN UNIDAD 
        ON INVENTARIO.UNIDAD = UNIDAD.UNIDAD  
    LEFT JOIN ESTADODEVOLUTIVO 
        ON DEVOLUTIVO.ESTADO = ESTADODEVOLUTIVO.ESTADO
        WHERE  DEVOLUTIVO.COMPANIA = s$compania$s
       AND  DEPENDENCIA.CODIGO BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
ORDER BY DEVOLUTIVO.COMPANIA,
        DEVOLUTIVO.ELEMENTO,
        DEVOLUTIVO.SERIE
