WITH VISTA_CUENTAALMACEN AS (SELECT V_BASE_CUENTAALMACEN.COMPANIA,   
       V_BASE_CUENTAALMACEN.TIPO,   
       V_BASE_CUENTAALMACEN.GRUPO,   
       V_BASE_CUENTAALMACEN.NOMBRE_DEPENDENCIA,  
       V_BASE_CUENTAALMACEN.BODEGA_DESTINO1,
       SUM(CASE WHEN (V_BASE_CUENTAALMACEN.FECHA < s$fechaInicial$s)   
                 THEN NVL(V_BASE_CUENTAALMACEN.VALOR_ENTRADAS,0)- NVL(V_BASE_CUENTAALMACEN.VALOR_SALIDAS,0)  
                    +NVL(V_BASE_CUENTAALMACEN.AJUSTESDEBITO,0)  + NVL(V_BASE_CUENTAALMACEN.AJUSTESCREDITO,0) ELSE 0 END ) AS SALDOANTERIOR,  
       SUM (CASE WHEN (V_BASE_CUENTAALMACEN.FECHA > s$fechaInicial$s)   
                 THEN NVL(V_BASE_CUENTAALMACEN.VALOR_ENTRADAS,0) ELSE 0 END ) AS EENTRADAS,  
       SUM(CASE WHEN (V_BASE_CUENTAALMACEN.FECHA > s$fechaInicial$s)   
                THEN NVL(V_BASE_CUENTAALMACEN.VALOR_SALIDAS,0) ELSE 0 END ) AS SSALIDAS,  
       SUM(CASE WHEN (V_BASE_CUENTAALMACEN.FECHA > s$fechaInicial$s)  
                  THEN NVL(V_BASE_CUENTAALMACEN.AJUSTESDEBITO,0) ELSE 0 END ) AS AAJUSTESDEBITO,   
       SUM(CASE WHEN (V_BASE_CUENTAALMACEN.FECHA > s$fechaInicial$s)  
                THEN NVL(V_BASE_CUENTAALMACEN.AJUSTESCREDITO,0) ELSE 0 END ) AS AAJUSTESCREDITO,  
       SUM(NVL(V_BASE_CUENTAALMACEN.VALOR_ENTRADAS,0)-NVL(V_BASE_CUENTAALMACEN.VALOR_SALIDAS,0)+   
             NVL(V_BASE_CUENTAALMACEN.AJUSTESDEBITO,0) +NVL(V_BASE_CUENTAALMACEN.AJUSTESCREDITO,0)) AS SALDOFINAL,   
       SUM(CASE WHEN (V_BASE_CUENTAALMACEN.FECHA < s$fechaInicial$s)  
                THEN NVL(V_BASE_CUENTAALMACEN.ENTRADAS,0) - NVL (V_BASE_CUENTAALMACEN.SALIDAS,0)+   
                     NVL(V_BASE_CUENTAALMACEN.CANTIDADAJUSTESDEBITO,0)+ NVL(V_BASE_CUENTAALMACEN.CANTIDADAJUSTESCREDITO,0) ELSE 0 END ) AS CANTIDADSALDOANTERIOR,   
       SUM(CASE WHEN (V_BASE_CUENTAALMACEN.FECHA > s$fechaInicial$s)  
              THEN NVL(V_BASE_CUENTAALMACEN.ENTRADAS,0) ELSE 0 END) AS CANTIDADENTRADAS,   
       SUM(CASE WHEN (V_BASE_CUENTAALMACEN.FECHA > s$fechaInicial$s)  
                THEN NVL(V_BASE_CUENTAALMACEN.SALIDAS,0) ELSE 0 END ) AS CANTIDADSALIDAS,   
       SUM(CASE WHEN (V_BASE_CUENTAALMACEN.FECHA > s$fechaInicial$s)  
                THEN NVL(V_BASE_CUENTAALMACEN.CANTIDADAJUSTESDEBITO,0)ELSE 0 END) AS CCANTIDADAJUSTESDEBITO,   
       SUM(CASE WHEN (V_BASE_CUENTAALMACEN.FECHA > s$fechaInicial$s)  
                THEN NVL(V_BASE_CUENTAALMACEN.CANTIDADAJUSTESCREDITO,0) ELSE 0 END) AS CCANTIDADAJUSTESCREDITO,   
       SUM(NVL(V_BASE_CUENTAALMACEN.ENTRADAS,0) - NVL(V_BASE_CUENTAALMACEN.SALIDAS,0) +   
           NVL(V_BASE_CUENTAALMACEN.CANTIDADAJUSTESDEBITO,0)+ NVL(V_BASE_CUENTAALMACEN.CANTIDADAJUSTESCREDITO,0)) AS CANTIDADSALDONUEVO  
FROM V_BASE_CUENTAALMACEN  
WHERE V_BASE_CUENTAALMACEN.COMPANIA= s$compania$s  
  AND V_BASE_CUENTAALMACEN.FECHA BETWEEN s$fechaCorte$s AND s$fechaFinal$s    
  AND V_BASE_CUENTAALMACEN.IND_REG NOT IN (0)  
GROUP BY  V_BASE_CUENTAALMACEN.COMPANIA,   
          V_BASE_CUENTAALMACEN.TIPO,   
          V_BASE_CUENTAALMACEN.GRUPO,   
          V_BASE_CUENTAALMACEN.NOMBRE_DEPENDENCIA,
          V_BASE_CUENTAALMACEN.BODEGA_DESTINO1
          )  
SELECT  VISTA_CUENTAALMACEN.GRUPO,   
        INVENTARIO.NOMBRELARGO,  
        VISTA_CUENTAALMACEN.NOMBRE_DEPENDENCIA DEPEND,   
        DECODE(VISTA_CUENTAALMACEN.TIPO,'C','Consumo','D','Devolutivos','N','ActivosFijos','M','Consumo Controlado','E','Recibidos en Comodato','S','Servicios','EC','Entregados en Comodato') TIPO,  
        SUM(VISTA_CUENTAALMACEN.SALDOANTERIOR  ) SUMADESALDOANTERIOR,   
        SUM(VISTA_CUENTAALMACEN.EENTRADAS      ) SUMADEENTRADAS,  
        SUM(VISTA_CUENTAALMACEN.SSALIDAS       ) SUMADESALIDAS ,  
        SUM(VISTA_CUENTAALMACEN.AAJUSTESDEBITO ) SUMADEAJUSTESDEBITO,  
        SUM(VISTA_CUENTAALMACEN.AAJUSTESCREDITO) SUMADEAJUSTESCREDITO,  
        SUM(VISTA_CUENTAALMACEN.SALDOFINAL     ) SUMADESALDOFINAL  
FROM VISTA_CUENTAALMACEN INNER JOIN INVENTARIO   
  ON VISTA_CUENTAALMACEN.COMPANIA = INVENTARIO.COMPANIA   
 AND VISTA_CUENTAALMACEN.GRUPO    = INVENTARIO.CODIGOELEMENTO
INNER JOIN BODEGA  
  ON VISTA_CUENTAALMACEN.COMPANIA        = BODEGA.COMPANIA 
 AND VISTA_CUENTAALMACEN.BODEGA_DESTINO1 = BODEGA.CODIGO   
WHERE BODEGA.MOSTRAR NOT IN(0)
GROUP BY VISTA_CUENTAALMACEN.GRUPO,   
        INVENTARIO.NOMBRELARGO,  
        VISTA_CUENTAALMACEN.NOMBRE_DEPENDENCIA ,   
        DECODE(VISTA_CUENTAALMACEN.TIPO,'C','Consumo','D','Devolutivos','N','ActivosFijos','M','Consumo Controlado','E','Recibidos en Comodato','S','Servicios','EC','Entregados en Comodato')  
