MERGE INTO CONSULTAS FIN USING (SELECT '001943RelacionOrdenes' INFORME ,TO_CLOB(q'[SELECT 
    D_ORDENDECOMPRA.CLASEORDEN,
    D_ORDENDECOMPRA.ORDENDECOMPRA,
    D_ORDENDECOMPRA.CODIGO,
    D_ORDENDECOMPRA.ELEMENTO,
    TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY')AS FECHA,
    INVENTARIO.NOMBRELARGO
    || ' - '
    || ESPECIFICACION  CONCEPTO,
    TERCERO.NOMBRE,
    D_ORDENDECOMPRA.CANTIDAD,
    D_ORDENDECOMPRA.SALDOCANT,
    D_ORDENDECOMPRA.VALORUNITARIO,
    D_ORDENDECOMPRA.VLRIVA,
    D_ORDENDECOMPRA.VLRTOTAL,
    ORDENDECOMPRA.NOTAS,
    TERCERO.NIT
  FROM D_ORDENDECOMPRA
  LEFT JOIN ORDENDECOMPRA
  ON D_ORDENDECOMPRA.ORDENDECOMPRA = ORDENDECOMPRA.NUMERO
  AND D_ORDENDECOMPRA.CLASEORDEN   = ORDENDECOMPRA.CLASEORDEN
  AND D_ORDENDECOMPRA.COMPANIA     = ORDENDECOMPRA.COMPANIA
  LEFT JOIN INVENTARIO
  ON D_ORDENDECOMPRA.COMPANIA  = INVENTARIO.COMPANIA
  AND D_ORDENDECOMPRA.ELEMENTO = INVENTARIO.CODIGOELEMENTO
  LEFT JOIN TERCERO
  ON ORDENDECOMPRA.SUCURSAL  = TERCERO.SUCURSAL
  AND ORDENDECOMPRA.TERCERO  = TERCERO.NIT
  AND ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA
  INNER JOIN TIPOORDENDECOMPRA
  ON D_ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA
  AND D_ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO
 WHERE D_ORDENDECOMPRA.COMPANIA = s$compania$s
  AND TIPOORDENDECOMPRA.CODIGO = 's$tipoContrato$s'
  AND ORDENDECOMPRA.FECHA BETWEEN 's$fechaIni$s' AND 's$fechaFin$s'
  ORDER BY D_ORDENDECOMPRA.COMPANIA,
    D_ORDENDECOMPRA.CLASEORDEN,
    D_ORDENDECOMPRA.ORDENDECOMPRA,
    D_ORDENDECOMPRA.CODIGO]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);