SELECT 
   CAMBIOSDEPLACA.COMPANIA,
   CAMBIOSDEPLACA.CAMBIO,
   CAMBIOSDEPLACA.OBSERVACIONES,
   CAMBIOSDEPLACA.USUARIO,
   CAMBIOSDEPLACA.FECHACAMBIO,
   D_CAMBIOSDEPLACA.ELEMENTO,
   INVENTARIO.NOMBRECORTO AS NOMBREELEMENTO,
   D_CAMBIOSDEPLACA.SERIE,
   D_CAMBIOSDEPLACA.PLACANUEVA,
   D_CAMBIOSDEPLACA.NUEVOELEMENTO,
   INVENTARIO_1.NOMBRECORTO AS NOMBRENUEVOELEMENTO  
FROM CAMBIOSDEPLACA
  INNER JOIN D_CAMBIOSDEPLACA
    ON CAMBIOSDEPLACA.COMPANIA = D_CAMBIOSDEPLACA.COMPANIA
   AND CAMBIOSDEPLACA.CAMBIO   = D_CAMBIOSDEPLACA.CAMBIO  
  INNER JOIN INVENTARIO  
    ON D_CAMBIOSDEPLACA.COMPANIA  = INVENTARIO.COMPANIA
    AND D_CAMBIOSDEPLACA.ELEMENTO = INVENTARIO.CODIGOELEMENTO
  INNER JOIN INVENTARIO  INVENTARIO_1
    ON D_CAMBIOSDEPLACA.COMPANIA       = INVENTARIO_1.COMPANIA
    AND D_CAMBIOSDEPLACA.NUEVOELEMENTO = INVENTARIO_1.CODIGOELEMENTO  
WHERE CAMBIOSDEPLACA.COMPANIA = s$compania$s
    AND CAMBIOSDEPLACA.CAMBIO = s$cambio$s    
