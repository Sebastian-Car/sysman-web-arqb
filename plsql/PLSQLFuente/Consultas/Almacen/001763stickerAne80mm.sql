SELECT DEVOLUTIVO.ELEMENTO,
  DEVOLUTIVO.SERIE,
  PCK_CODIGODEBARRAS.FC_IMPRIMIRCODIGODEBARRAS(CHR(205)
  ||CHR(102) 
  ||PCK_SYSMAN_UTL.FC_STRZERO(DEVOLUTIVO.SERIE,6)) AS CODIGODEBARRAS_BIN,
  PCK_SYSMAN_UTL.FC_STRZERO(DEVOLUTIVO.SERIE,6) BARRAS
FROM DEVOLUTIVO
	s$variableInner$s		
WHERE DEVOLUTIVO.COMPANIA     = s$compania$s 
  AND DEVOLUTIVO.PLACAANULADA = 0
  AND s$condicion$s
ORDER BY s$orden$s
