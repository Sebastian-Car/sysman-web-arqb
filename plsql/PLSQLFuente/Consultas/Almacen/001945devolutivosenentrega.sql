SELECT DEPENDENCIA,
    INVENTARIO.NOMBRELARGO,
    DEVOLUTIVO.SERIE,
    TRIM(INVENTARIO.NOMBRELARGO)
    || ' -'
    || DEVOLUTIVO.DESCRIPCION NOMBRELARGOS,
    DEVOLUTIVO.SERIEDEVOLUTIVO,
    ESTADODEVOLUTIVO.DESCRIPCION DESCRIPCIONESTADO,
    DEVOLUTIVO.VALOR VALORDEVOLUTIVO
  FROM DEVOLUTIVO
  LEFT JOIN INVENTARIO
  ON DEVOLUTIVO.COMPANIA  = INVENTARIO.COMPANIA
  AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
  LEFT JOIN ESTADODEVOLUTIVO
  ON DEVOLUTIVO.ESTADO      = ESTADODEVOLUTIVO.ESTADO
  WHERE DEVOLUTIVO.COMPANIA = s$compania$s
  AND DEVOLUTIVO.DEPENDENCIA BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s'
  GROUP BY DEPENDENCIA,
    DEVOLUTIVO.SERIE,
    INVENTARIO.NOMBRELARGO,
    TRIM(INVENTARIO.NOMBRELARGO)
    || ' -'
    || DEVOLUTIVO.DESCRIPCION ,
    DEVOLUTIVO.SERIEDEVOLUTIVO,
    ESTADODEVOLUTIVO.DESCRIPCION,
    DEVOLUTIVO.VALOR
  ORDER BY DEVOLUTIVO.SERIE,
    INVENTARIO.NOMBRELARGO
