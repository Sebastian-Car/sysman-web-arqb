SELECT 
  C_MOVIMIENTODEVOLUTIVO.COMPANIA,
  C_MOVIMIENTODEVOLUTIVO.TIPOMOVIMIENTO,
  C_MOVIMIENTODEVOLUTIVO.MOVIMIENTO,
  C_MOVIMIENTODEVOLUTIVO.ELEMENTO,
  C_MOVIMIENTODEVOLUTIVO.SERIE,
  TO_CHAR(C_MOVIMIENTODEVOLUTIVO.FECHA,'DD/MM/YYYY') FECHA,
  C_MOVIMIENTODEVOLUTIVO.HORA,
  C_MOVIMIENTODEVOLUTIVO.IND_REG,
  C_MOVIMIENTODEVOLUTIVO.RESPON,
  C_MOVIMIENTODEVOLUTIVO.DEPEN,
  C_MOVIMIENTODEVOLUTIVO.VALORUNITARIO,
  C_MOVIMIENTODEVOLUTIVO.NOMBRE,
  C_MOVIMIENTODEVOLUTIVO.TIPOELEMENTO,
  C_MOVIMIENTODEVOLUTIVO.VALORUNITARIO,
  C_MOVIMIENTODEVOLUTIVO.ELEM,
  C_MOVIMIENTODEVOLUTIVO.CLASE,
  C_MOVIMIENTODEVOLUTIVO.NUMERO_POLIZA,
  C_MOVIMIENTODEVOLUTIVO.ASEGURADORA,
  TO_CHAR(C_MOVIMIENTODEVOLUTIVO.FECHAI,'DD/MM/YYYY') FECHAI,
  TO_CHAR(C_MOVIMIENTODEVOLUTIVO.FECHAF ,'DD/MM/YYYY') FECHAF,
  DEPENDENCIA.NOMBRE NOMDEPEN,
  PCK_ALMACEN.FC_VERIFICAR_NOMBRE_DEVOLUTIVO(2,0,RESPONSABLE.CEDULA,C_MOVIMIENTODEVOLUTIVO.COMPANIA) NOMRESPO,
  DEVOLUTIVO.SERIEDEVOLUTIVO,
  DEVOLUTIVO.DESCRIPCION,
  C_MOVIMIENTODEVOLUTIVO.NOMBRE_ASEGURADORA
FROM (SELECT D_MOVIMIENTO.COMPANIA,
        D_MOVIMIENTO.TIPOMOVIMIENTO,
        D_MOVIMIENTO.MOVIMIENTO,
        D_MOVIMIENTO.ELEMENTO,
        D_MOVIMIENTO.SERIE,
        D_MOVIMIENTO.FECHA,
        TO_CHAR(D_MOVIMIENTO.HORA,'HH24:MI:SS') HORA,
        D_MOVIMIENTO.IND_REG,
        MOVIMIENTO.RESPONSABLE_DESTINO RESPON,
        MOVIMIENTO.DEPENDENCIA_DESTINO DEPEN,
        TIPOMOVIMIENTO.NOMBRE,
        TIPOMOVIMIENTO.TIPOELEMENTO,
        D_MOVIMIENTO.VALORUNITARIO,
        D_MOVIMIENTO.ELEMENTO ELEM,
        TIPOMOVIMIENTO.CLASE,
        D_POLIZAS_ACTIVOS.ASEGURADORA,
        MAX(D_POLIZAS_ACTIVOS.NUMERO_POLIZA) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NUMERO_POLIZA,
        MAX(D_POLIZAS_ACTIVOS.FECHAI) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) FECHAI,
        MAX(D_POLIZAS_ACTIVOS.FECHAF) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) FECHAF,
        ASEGURADORA.NOMBRE NOMBRE_ASEGURADORA
      FROM DEVOLUTIVO
        INNER JOIN D_MOVIMIENTO 
           ON DEVOLUTIVO.COMPANIA = D_MOVIMIENTO.COMPANIA
          AND DEVOLUTIVO.SERIE    = D_MOVIMIENTO.SERIE
          AND DEVOLUTIVO.ELEMENTO = D_MOVIMIENTO.ELEMENTO
        INNER JOIN MOVIMIENTO
           ON D_MOVIMIENTO.COMPANIA       = MOVIMIENTO.COMPANIA      
          AND D_MOVIMIENTO.MOVIMIENTO     = MOVIMIENTO.NUMERO
          AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO
        INNER JOIN TIPOMOVIMIENTO
           ON D_MOVIMIENTO.COMPANIA       = TIPOMOVIMIENTO.COMPANIA
          AND D_MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
        LEFT JOIN D_POLIZAS_ACTIVOS
           ON DEVOLUTIVO.COMPANIA = D_POLIZAS_ACTIVOS.COMPANIA 
          AND DEVOLUTIVO.SERIE    = D_POLIZAS_ACTIVOS.SERIE 
          AND DEVOLUTIVO.ELEMENTO = D_POLIZAS_ACTIVOS.ELEMENTO
        LEFT JOIN ASEGURADORA 
          ON D_POLIZAS_ACTIVOS.ASEGURADORA = ASEGURADORA.NITASEGURADORA
      WHERE D_MOVIMIENTO.COMPANIA       = s$compania$s
        AND D_MOVIMIENTO.SERIE          = s$serie$s
        AND TIPOMOVIMIENTO.TIPOELEMENTO <>'C'
      GROUP BY D_MOVIMIENTO.COMPANIA,
        D_MOVIMIENTO.TIPOMOVIMIENTO,
        D_MOVIMIENTO.MOVIMIENTO,
        D_MOVIMIENTO.ELEMENTO,
        D_MOVIMIENTO.SERIE,
        D_MOVIMIENTO.FECHA,
        TO_CHAR(D_MOVIMIENTO.HORA,'HH24:MI:SS'),
        D_MOVIMIENTO.IND_REG,
        MOVIMIENTO.RESPONSABLE_DESTINO,
        MOVIMIENTO.DEPENDENCIA_DESTINO,
        D_MOVIMIENTO.VALORUNITARIO,
        TIPOMOVIMIENTO.NOMBRE,
        TIPOMOVIMIENTO.TIPOELEMENTO,
        D_MOVIMIENTO.VALORUNITARIO,
        D_MOVIMIENTO.ELEMENTO,
        TIPOMOVIMIENTO.CLASE,
        D_POLIZAS_ACTIVOS.ASEGURADORA,
        ASEGURADORA.NOMBRE
      ORDER BY D_MOVIMIENTO.COMPANIA,
        D_MOVIMIENTO.SERIE,
        D_MOVIMIENTO.FECHA,
        TO_CHAR(D_MOVIMIENTO.HORA,'HH24:MI:SS')) C_MOVIMIENTODEVOLUTIVO
LEFT JOIN DEPENDENCIA
   ON C_MOVIMIENTODEVOLUTIVO.COMPANIA = DEPENDENCIA.COMPANIA
  AND C_MOVIMIENTODEVOLUTIVO.DEPEN    = DEPENDENCIA.CODIGO
LEFT JOIN RESPONSABLE
   ON C_MOVIMIENTODEVOLUTIVO.COMPANIA = RESPONSABLE.COMPANIA
  AND C_MOVIMIENTODEVOLUTIVO.RESPON   = RESPONSABLE.CEDULA
LEFT JOIN DEVOLUTIVO
   ON C_MOVIMIENTODEVOLUTIVO.COMPANIA = DEVOLUTIVO.COMPANIA
  AND C_MOVIMIENTODEVOLUTIVO.ELEMENTO = DEVOLUTIVO.ELEMENTO
  AND C_MOVIMIENTODEVOLUTIVO.SERIE    = DEVOLUTIVO.SERIE
WHERE C_MOVIMIENTODEVOLUTIVO.COMPANIA = s$compania$s
  AND C_MOVIMIENTODEVOLUTIVO.SERIE    = s$serie$s
ORDER BY
  C_MOVIMIENTODEVOLUTIVO.COMPANIA,
  C_MOVIMIENTODEVOLUTIVO.SERIE,
  C_MOVIMIENTODEVOLUTIVO.FECHA,
  C_MOVIMIENTODEVOLUTIVO.HORA