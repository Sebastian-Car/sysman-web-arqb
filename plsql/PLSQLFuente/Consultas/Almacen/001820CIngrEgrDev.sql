SELECT INVENTARIO.COMPANIA,
       INVENTARIO.NOMBRELARGO,
       SUM(C_MOVENTDEVOLUTIVO.TOTAL) TOTAL,
       C_MOVENTDEVOLUTIVO.GRUPO AGRUPACION,
       C_MOVENTDEVOLUTIVO.TIPOMOVIMIENTO,
       TIPOMOVIMIENTO.NOMBRECORTO
  FROM INVENTARIO
  LEFT JOIN (SELECT D_MOVIMIENTO.COMPANIA,
       D_MOVIMIENTO.TIPOMOVIMIENTO,
       D_MOVIMIENTO.MOVIMIENTO,
       SUBSTR(D_MOVIMIENTO.ELEMENTO, 0 ,s$digitos$s) GRUPO,
       SUM(D_MOVIMIENTO.VALORTOTAL) TOTAL,
       D_MOVIMIENTO.FECHA,
       TIPOMOVIMIENTO.CONCEPTO
  FROM D_MOVIMIENTO
  LEFT JOIN TIPOMOVIMIENTO
    ON D_MOVIMIENTO.COMPANIA        = TIPOMOVIMIENTO.COMPANIA
   AND D_MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
 WHERE D_MOVIMIENTO.COMPANIA     = 's$compania$s'
   AND D_MOVIMIENTO.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
   AND TIPOMOVIMIENTO.CONCEPTO     IN ('DS','L')
   AND D_MOVIMIENTO.TIPOMOVIMIENTO IN ('SDF','RDI','TDR')
 GROUP BY D_MOVIMIENTO.COMPANIA,
       D_MOVIMIENTO.TIPOMOVIMIENTO,
       D_MOVIMIENTO.MOVIMIENTO,
       SUBSTR(D_MOVIMIENTO.ELEMENTO, 0 , s$digitos$s),
       D_MOVIMIENTO.FECHA,
       TIPOMOVIMIENTO.CONCEPTO) C_MOVENTDEVOLUTIVO 
    ON INVENTARIO.COMPANIA = C_MOVENTDEVOLUTIVO.COMPANIA
   AND INVENTARIO.CODIGOELEMENTO                 = C_MOVENTDEVOLUTIVO.GRUPO
  LEFT JOIN TIPOMOVIMIENTO
    ON C_MOVENTDEVOLUTIVO.COMPANIA        = TIPOMOVIMIENTO.COMPANIA
   AND C_MOVENTDEVOLUTIVO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
 WHERE INVENTARIO.COMPANIA             = 's$compania$s'
   AND LENGTH(INVENTARIO.CODIGOELEMENTO) = s$digitos$s
   AND C_MOVENTDEVOLUTIVO.GRUPO         IS NOT NULL
   AND INVENTARIO.TIPO                   = 's$tipo$s'
 GROUP BY INVENTARIO.COMPANIA,
       INVENTARIO.NOMBRELARGO,
       C_MOVENTDEVOLUTIVO.GRUPO ,
       C_MOVENTDEVOLUTIVO.TIPOMOVIMIENTO,
       TIPOMOVIMIENTO.NOMBRECORTO
 UNION ALL
SELECT D_MOVIMIENTO.COMPANIA,
       INVENTARIO.NOMBRELARGO,
       SUM(D_MOVIMIENTO.VALORTOTAL) TOTAL,
       SUBSTR(D_MOVIMIENTO.ELEMENTO, 0 , s$digitos$s) AGRUPACION,
       D_MOVIMIENTO.TIPOMOVIMIENTO,
       TIPOMOVIMIENTO.NOMBRECORTO
  FROM INVENTARIO
 INNER JOIN D_MOVIMIENTO
    ON INVENTARIO.COMPANIA        = D_MOVIMIENTO.COMPANIA
   AND INVENTARIO.CODIGOELEMENTO = D_MOVIMIENTO.ELEMENTO
  LEFT JOIN TIPOMOVIMIENTO
    ON D_MOVIMIENTO.COMPANIA        = TIPOMOVIMIENTO.COMPANIA
   AND D_MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
 WHERE D_MOVIMIENTO.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
   AND TIPOMOVIMIENTO.CLASE        = 's$clase$s'
   AND TIPOMOVIMIENTO.TIPOELEMENTO = 's$tipo$s'
   AND TIPOMOVIMIENTO.CONCEPTO    IN ('R')
 GROUP BY D_MOVIMIENTO.COMPANIA,
       INVENTARIO.NOMBRELARGO,
       SUBSTR(D_MOVIMIENTO.ELEMENTO, 0 , s$digitos$s) ,
       D_MOVIMIENTO.TIPOMOVIMIENTO,
       TIPOMOVIMIENTO.NOMBRECORTO
