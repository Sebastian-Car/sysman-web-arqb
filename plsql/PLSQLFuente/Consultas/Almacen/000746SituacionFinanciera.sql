SELECT LENGTH(ID_NIIF) AS NIVEL, 
       s$codigo$s AS CODIGO, 
       REPLACE(NOMBRE,',',';') AS CUENTA, 
       SALDOINICIAL AS SALDOS_PCGA, 
       SUM(TMP_SITUACION_APERTURA_NIIF.REVALOR_DEBITO) AS RECLASIFICACIONES_DEBITO, 
       SUM(TMP_SITUACION_APERTURA_NIIF.REVALOR_CREDITO) AS RECLASIFICACIONES_CREDITO,
       SUM(TMP_SITUACION_APERTURA_NIIF.AENVALOR_DEBITO) AS AJUSTESPORERRORES_DEBITO, 
       SUM(TMP_SITUACION_APERTURA_NIIF.AENVALOR_CREDITO) AS AJUSTESPORERRORES_CREDITO, 
       SUM(TMP_SITUACION_APERTURA_NIIF.ACNVALOR_DEBITO) AS AJUSTESPORCONVERGENCIA_DEBITO, 
       SUM(TMP_SITUACION_APERTURA_NIIF.ACNVALOR_CREDITO) AS AJUSTESPORCONVERGENCIA_CREDITO, 
       SALDOFINAL AS SALDOS_NIIF, 
       CODIGO_NIIF
FROM (WITH MOVIMIENTOS_RECLASIFICACION AS
          (SELECT 
                  DETALLE_COMPROBANTE_CNT.COMPANIA, 
                  DETALLE_COMPROBANTE_CNT.ANO,
                  DETALLE_COMPROBANTE_CNT.CUENTA, 
                  SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO) AS SVALOR_DEBITO, 
                  SUM(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO) AS SVALOR_CREDITO, 
                  DETALLE_COMPROBANTE_CNT.TIPO_CPTE
             FROM DETALLE_COMPROBANTE_CNT INNER JOIN TIPO_COMPROBANTE 
                    ON DETALLE_COMPROBANTE_CNT.COMPANIA  = TIPO_COMPROBANTE.COMPANIA  
                   AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
            WHERE TIPO_COMPROBANTE.CLASE_CONTABLE IN ('Z') 
              AND TIPO_COMPROBANTE.CODIGO NOT IN ('CIE')
              AND DETALLE_COMPROBANTE_CNT.COMPANIA = s$compania$s 
              AND DETALLE_COMPROBANTE_CNT.ANO = PCK_SYSMAN_UTL.FC_ANIO(SYSDATE)
            GROUP BY DETALLE_COMPROBANTE_CNT.COMPANIA, DETALLE_COMPROBANTE_CNT.ANO, DETALLE_COMPROBANTE_CNT.CUENTA, DETALLE_COMPROBANTE_CNT.TIPO_CPTE
          )
          SELECT PLAN_CONTABLE.SALDOs$mesTrabajo$s AS SALDOINICIAL, 
                 PLAN_CONTABLE.COMPANIA, 
                 PLAN_CONTABLE.ANO, 
                 PLAN_CONTABLE.CODIGO, 
                 CASE WHEN PLAN_CONTABLE.CODIGO_NIIF IS NULL 
                      THEN PLAN_CONTABLE.CODIGO 
                      ELSE PLAN_CONTABLE.CODIGO_NIIF 
                 END ID_NIIF,
                 CODIGO_NIIF,
                 CASE WHEN MOVIMIENTOS_RECLASIFICACION.TIPO_CPTE = 's$parAenValor$s' 
                      THEN MOVIMIENTOS_RECLASIFICACION.SVALOR_DEBITO
                      ELSE 0
                 END AENVALOR_DEBITO,
                 CASE WHEN MOVIMIENTOS_RECLASIFICACION.TIPO_CPTE = 's$parAenValor$s' 
                      THEN MOVIMIENTOS_RECLASIFICACION.SVALOR_CREDITO
                      ELSE 0
                 END AENVALOR_CREDITO,
                 CASE WHEN MOVIMIENTOS_RECLASIFICACION.TIPO_CPTE = 's$parAcnValor$s' 
                      THEN MOVIMIENTOS_RECLASIFICACION.SVALOR_DEBITO
                      ELSE 0
                 END ACNVALOR_DEBITO,
                 CASE WHEN MOVIMIENTOS_RECLASIFICACION.TIPO_CPTE = 's$parAcnValor$s' 
                      THEN MOVIMIENTOS_RECLASIFICACION.SVALOR_CREDITO
                      ELSE 0
                 END ACNVALOR_CREDITO,
                 CASE WHEN MOVIMIENTOS_RECLASIFICACION.TIPO_CPTE = 's$parReValor$s' 
                      THEN MOVIMIENTOS_RECLASIFICACION.SVALOR_DEBITO
                      ELSE 0
                 END REVALOR_DEBITO,
                 CASE WHEN MOVIMIENTOS_RECLASIFICACION.TIPO_CPTE = 's$parReValor$s' 
                      THEN MOVIMIENTOS_RECLASIFICACION.SVALOR_CREDITO
                      ELSE 0
                 END REVALOR_CREDITO,
                 PLAN_CONTABLE.NOMBRE,  
                 PLAN_CONTABLE.SALDOs$mes$s AS SALDOFINAL  
          FROM V_PLAN_CONTABLE PLAN_CONTABLE LEFT JOIN MOVIMIENTOS_RECLASIFICACION 
                 ON PLAN_CONTABLE.COMPANIA = MOVIMIENTOS_RECLASIFICACION.COMPANIA  
                AND PLAN_CONTABLE.ID       = MOVIMIENTOS_RECLASIFICACION.CUENTA 
                AND PLAN_CONTABLE.ANO      = MOVIMIENTOS_RECLASIFICACION.ANO   
          WHERE PLAN_CONTABLE.COMPANIA = s$compania$s 
            AND PLAN_CONTABLE.ANO      = s$anio$s 
            AND PLAN_CONTABLE.CODIGO BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 
            AND LENGTH(PLAN_CONTABLE.CODIGO)<= s$digitos$s
            AND PLAN_CONTABLE.AUXILIAR IS NULL
            AND PLAN_CONTABLE.CENTRO_COSTO IS NULL
            AND TERCERO IS NULL
            AND (PLAN_CONTABLE.SALDOs$mes$s NOT IN (0) 
             OR  PLAN_CONTABLE.DEBITOs$mes$s NOT IN (0) 
             OR  PLAN_CONTABLE.CREDITOs$mes$s NOT IN (0) 
             OR  PLAN_CONTABLE.SALDOs$mesTrabajo$s NOT IN (0))
        )  TMP_SITUACION_APERTURA_NIIF
GROUP BY LENGTH(ID_NIIF), ID_NIIF, NOMBRE, SALDOINICIAL, SALDOFINAL, s$codigo$s, CODIGO_NIIF
s$orderBy$s
