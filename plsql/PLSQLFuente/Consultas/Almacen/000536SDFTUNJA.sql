SELECT 
      V.COMPANIA, 
      V.TIPOMOVIMIENTO,
      V.MOVIMIENTO,
      V.ELEMENTO AS CODIGOELEMENTO, 
      V.ESPECIFICACION,  
      V.TIPOMOVIMIENTO_NOM TIPOMOV, 
      V.NOMBRELARGO, 
      V.UNIDAD, 
      V.CANTIDAD, 
      V.VALORUNITARIO,
      V.VALORTOTAL_D VALORTOTAL,
      V.VALORTOTAL_MOV,
      V.FECHA_MOV FECHA, 
      V.TERCERO, 
      V.MOVASOCIADO, 
      V.FECHAMOVASOCIADO,
      V.DEPENDENCIA_DESTINO DEPENDENCIA, 
      V.DEPENDENCIA_ORIGEN DEPENDENCIAANT,
      V.DESCRIPCION_MOV DESCRIPCION, 
      V.DESCRIPCION_INV DESCRIPCIONINV,
      V.VALORTOTAL_MOV AS VLRTOTAL,
      V.DEPENDENCIA_DESTINO_NOM NOMBREDEPENDENCIA, 
      V.TERCERO_NOM NOMBRERESPONSABLE, 
      V.TERCERO_TEL TELEFONOS, 
      V.TERCERO_FAX FAX, 
      V.TERCERO_DIR DIRECCION,
      V.RESPONSABLE_DESTINO CEDULA, 
      V.CENTRODECOSTO_NOM NOMBRECENTROCOSTO,
      V.CENTRODECOSTO,
      V.RESPONSABLE_ORIGEN_NOM RESPONSABLEANTERIOR,
      V.RESPONSABLE_DESTINO_NOM RESPONSABLENUEVO,
      V.DEPENDENCIA_ORIGEN || ' ' || V.DEPENDENCIA_ORIGEN_NOM NOMBREDEPENDENCIAANT, 
      V.DEPENDENCIA_DESTINO || ' ' || V.DEPENDENCIA_DESTINO_NOM NOMBREDEPENDENCIANUEVA,
      NVL(V.VALORDOCASOCIADO,0) AS VALORDOCASOCIADO, 
      V.UNIDAD_NOM NOMBREUNIDAD, 
      V.AJUSTECENTAVOS1_MOV AJUSTECENTAVOS1, 
      V.TOTALCONAJUSTE_MOV TOTALCONAJUSTE,
      V.RESPONSABLE_DESTINO_CARGO AS CARGORESPONSABLE, 
      V.TIPOMOVIMIENTO || MOVIMIENTO AS GRUPO, 
      V.TIPOPESONA TIPOPERSONA,
      V.AJUSTECENTAVOS1_MOV AS AJUSTECENTAVOS,
      V.AJUSTECENTAVOS1_MOV*-1 AS AJUSTECENTAVOSM,
      V.AJUSTECENTAVOS_D AS DETALLEAJUSTE, 
      V.AJUSTE_DEC_MANUAL,
      V.TIPOMOVASOCIADO_NOM NOMBREDOCASOCIADO,
      V.ESCRITURA,
      V.NUMEROCATASTRAL, 
      V.MATINMOBILIARIA,
      V.COD_CHIP,
      R.NUMERO RESERVA,
      CASE WHEN  TRIM(V.IND_REG) IS NULL THEN 'NONUM' END AS NOREGISTRADO,
      CASE TRIM(V.SERIE) WHEN '' THEN NULL 
        ELSE V.SERIE 
      END SERIE,
      CASE TRIM(V.MARCA) WHEN '' THEN NULL
        ELSE V.MARCA 
      END AS MARCA,
      CASE V.CLASE WHEN 'E' THEN 'ENTRADA' ELSE 'SALIDA' END CLASEMOV,
      CASE TRIM(V.SERIEDEVOLUTIVO) WHEN '' THEN NULL
       ELSE V.SERIEDEVOLUTIVO END AS SERIEDEVOLUTIVO,
      CASE WHEN TRIM(V.MARCA) IS NULL OR TRIM(V.MARCA) = ' ' THEN '' || V.DESCRIPCION_INV
        ELSE 'MARCA: ' || V.MARCA || ' ' || V.DESCRIPCION_INV 
      END AS NOMBRELARGOSDF,
      CASE V.TIPOPESONA WHEN 'R' THEN V.TERCERO_NOM || ', C.C.' || V.TERCERO ELSE V.TERCERO_NOM || ', Tel:' ||  V.TERCERO_TEL
          || ' , Nit: ' || V.TERCERO END || CASE  WHEN TRIM(NVL(V.TERCERO_FAX,'')) IS NOT NULL THEN 'Fax: ' || V.TERCERO_FAX || ' '
        ELSE ''
       END RESPONSABLE,
      CASE V.TIPOPESONA WHEN 'R' THEN V.TERCERO_NOM || ', C.C.' || V.TERCERO 
        ELSE V.TERCERO_NOM || ', Tel:' || V.TERCERO_TEL || ' , Nit: ' ||  V.TERCERO END ||
      CASE WHEN TRIM(V.TERCERO_DIR) IS NULL OR TRIM(V.TERCERO_DIR) = ' '  THEN ' ' 
        ELSE ' Dirección: ' || V.TERCERO_DIR || ' ' END ||
      CASE WHEN TRIM(V.TERCERO_FAX) IS NULL OR TRIM(V.TERCERO_FAX) = ' ' THEN ''
        ELSE '  Fax: ' || V.TERCERO_FAX END INFPERSONA,
       CASE V.TIPOPESONA WHEN 'R' THEN 'ELEMENTOS A CARGO DE: ' ELSE 'PROVEEDOR' END CAMPO1,
      V.NOMBRELARGO || ' ' ||  
      CASE  WHEN TRIM(NVL(V.ESPECIFICACION,'')) IS NULL THEN ' ' 
        ELSE V.ESPECIFICACION 
       END || 
      CASE  WHEN TRIM(NVL(V.MARCA,'')) IS NOT NULL THEN 'MARCA: ' || V.MARCA || ' '
        ELSE ''
      END || 
      CASE  WHEN TRIM(NVL(V.SERIEDEVOLUTIVO,'')) IS NOT NULL THEN ', SERIE: ' || V.SERIEDEVOLUTIVO || ' ' 
        ELSE ''
      END ||
      CASE WHEN TRIM(NVL(V.SERIE,''))= 0 THEN ','
        ELSE ', PLACA: ' || V.SERIE
      END DESCRIPCIONELEMENTO,
      V.NOMBRELARGO || ' ' || 
      CASE WHEN V.ESPECIFICACION IS NULL THEN ' ' 
        ELSE V.ESPECIFICACION END NOMBREELEMENTO,
      V.NOMBRELARGO ||  ' ' || CASE TRIM(V.ESPECIFICACION) WHEN NULL THEN ' ' 
        ELSE V.ESPECIFICACION END ||
      CASE WHEN TRIM(V.MARCA) IS NOT NULL OR TRIM(V.MARCA )NOT IN '' THEN ', MARCA ' || V.MARCA || ''
        ELSE '' END || 
      CASE WHEN TRIM(V.SERIEDEVOLUTIVO) IS NULL OR V.SERIEDEVOLUTIVO = ' ' THEN '' 
        ELSE ', Serie: ' || V.SERIEDEVOLUTIVO END ||
      CASE WHEN TRIM(V.ESCRITURA) IS NULL  THEN ' '
        ELSE 'Escritura: ' || V.ESCRITURA END || 
      CASE WHEN TRIM(V.NUMEROCATASTRAL) IS  NULL THEN ' '
        ELSE 'No. Catastro: ' || V.NUMEROCATASTRAL END || 
      CASE WHEN TRIM(V.MATINMOBILIARIA) IS NULL THEN ' '
        ELSE 'Matrícula: ' || V.MATINMOBILIARIA END || 
      CASE WHEN TRIM(V.COD_CHIP) IS NULL THEN ' '
        ELSE 'Chip: ' || V.COD_CHIP END DESCRELEMENTO
FROM V_MOVIMIENTO_INFORME V
    LEFT JOIN V_ORDENDECOMPRA_RES R ON V.COMPANIA = R.COMPANIA AND V.TIPOMOVIMIENTO = R.TIPO
WHERE V.COMPANIA       = s$compania$s 
  AND V.TIPOMOVIMIENTO = 's$tipoMovimiento$s'
  AND V.MOVIMIENTO BETWEEN s$movimientoInicial$s AND s$movimientoFinal$s
ORDER BY V.COMPANIA, V.TIPOMOVIMIENTO, V.MOVIMIENTO, V.ELEMENTO
