            FROM DEPENDENCIA
            WHERE CLASE_BODEGA= 20
            AND COMPANIA      = s$compania$s
            ORDER BY CODIGO) CONS
 ON DEPENDENCIA_RESPONSABLE.COMPANIA = CONS.COMPANIA
AND DEPENDENCIA_RESPONSABLE.DEPENDENCIA = CONS.CODIGO
INNER JOIN TERCERO
    ON DEPENDENCIA_RESPONSABLE.COMPANIA   = TERCERO.COMPANIA
   AND DEPENDENCIA_RESPONSABLE.RESPONSABLE = TERCERO.NIT
WHERE DEPENDENCIA_RESPONSABLE.COMPANIA  = s$compania$s
AND RESPONSABLEALMACEN NOT             IN (0)
