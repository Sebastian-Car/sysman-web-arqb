MERGE INTO CONSULTAS FIN USING (SELECT '002880SubMOIUES' INFORME ,TO_CLOB(q'[SELECT DISTINCT
  D_MOVIMIENTO.TIPOMOVIMIENTO,
  D_MOVIMIENTO.MOVIMIENTO,
  SUBSTR(D_MOVIMIENTO.ELEMENTO,0,5) ELEMENTOEX,
  MOVIMIENTO.AJUSTE_DEC_MANUAL,
  SUM(D_MOVIMIENTO.VALORTOTAL)  SUMADEVALORTOTAL,
  SUM(D_MOVIMIENTO.VALORIVA)       SUMADEVALORIVA,
  SUM(D_MOVIMIENTO.VALORBASE)      SUMADEVALORBASE,
  SUM(D_MOVIMIENTO.VLRIMPCONSUMO)  SUMADEVLRIMPCONSUMO,
  SUM(D_MOVIMIENTO.VLR_AIU) SUMADEVLRAIU
FROM D_MOVIMIENTO
INNER JOIN MOVIMIENTO
ON D_MOVIMIENTO.COMPANIA = MOVIMIENTO.COMPANIA
AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO
AND D_MOVIMIENTO.MOVIMIENTO = MOVIMIENTO.NUMERO
WHERE D_MOVIMIENTO.COMPANIA          =  s$compania$s
  AND D_MOVIMIENTO.TIPOMOVIMIENTO = 's$tipoMovimiento$s'
  AND D_MOVIMIENTO.MOVIMIENTO         =s$movimientoInicial$s
GROUP BY
  D_MOVIMIENTO.TIPOMOVIMIENTO,
  D_MOVIMIENTO.MOVIMIENTO,
  MOVIMIENTO.AJUSTE_DEC_MANUAL,
  SUBSTR(D_MOVIMIENTO.ELEMENTO,0,5)]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
COMMIT;

MERGE INTO CONSULTAS_SUB FIN USING (SELECT '002879MOIUES' INFORME_PADRE, '002880SubMOIUES' INFORME_HIJO, 'PR_STRSQL_D_MOVIMIENTO' PARAMETRO ,NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (FIN.INFORME_PADRE = INI.INFORME_PADRE AND INI.INFORME_HIJO = FIN.INFORME_HIJO)  WHEN MATCHED THEN  UPDATE SET FIN.PARAMETRO = INI.PARAMETRO , FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME_PADRE,INFORME_HIJO, PARAMETRO,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME_PADRE,INI.INFORME_HIJO, INI.PARAMETRO,INI.CREATED_BY,SYSDATE);
COMMIT;
