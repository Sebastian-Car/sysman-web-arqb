SELECT D_TRANSACCION_DEV.SERIE_DEVOLUTIVO, 
       D_TRANSACCION_DEV.ELEMENTO_DEVOLUTIVO, 
       DEVOLUTIVO.DESCRIPCION 
FROM   D_TRANSACCION_DEV 
       INNER JOIN DEVOLUTIVO 
          ON  D_TRANSACCION_DEV.COMPANIA            = DEVOLUTIVO.COMPANIA 
          AND D_TRANSACCION_DEV.ELEMENTO_DEVOLUTIVO = DEVOLUTIVO.ELEMENTO 
          AND D_TRANSACCION_DEV.SERIE_DEVOLUTIVO    = DEVOLUTIVO.SERIE 
WHERE  D_TRANSACCION_DEV.COMPANIA           = $P{PR_COMPANIA}
  AND  D_TRANSACCION_DEV.TIPO_TRANSACCION   = $P{PR_TIPO_TRANSACCION}
  AND  D_TRANSACCION_DEV.CODIGO_TRANSACCION = $P{PR_CODIGO_TRANSACCION}
