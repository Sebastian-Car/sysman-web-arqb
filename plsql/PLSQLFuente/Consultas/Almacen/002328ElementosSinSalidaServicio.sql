SELECT
    DEVOLUTIVO.COMPANIA,
    DEVOLUTIVO.ELEMENTO,
    DEVOLUTIVO.SERIE,
    INVENTARIO.NOMBRELARGO NOMBRECOMPLETO,
    PCK_GENERALES.FC_NOMBREINVENTARIO(SUBSTR(DEVOLUTIVO.ELEMENTO,PCK_SYSMAN_UTL.FC_PAR(DEVOLUTIVO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',PCK_DATOS.FC_MODULOALMACEN,SYSDATE)),DEVOLUTIVO.COMPANIA,NULL) NOMBRE_GRUPO, 
    SUBSTR(DEVOLUTIVO.ELEMENTO,PCK_SYSMAN_UTL.FC_PAR(DEVOLUTIVO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',PCK_DATOS.FC_MODULOALMACEN,SYSDATE)) AS GRUPO,  
    TRIM(INVENTARIO.NOMBRELARGO)||' -'||DEVOLUTIVO.DESCRIPCION AS NOMBRELARGO,
DEVOLUTIVO.SERIEDEVOLUTIVO, 
ESTADODEVOLUTIVO.DESCRIPCION AS ESTADODEVOLUTIVO,
DEVOLUTIVO.VALOR AS VALORDEVOLUTIVO,
SUM(CASE WHEN CLASE ='S' THEN 1 ELSE 0 END) AS CLASES, 
INVENTARIO.TIPO,
DEVOLUTIVO.PLACAANULADA

FROM DEVOLUTIVO  
	LEFT JOIN INVENTARIO  
		ON  DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA  
		AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
  LEFT JOIN ESTADODEVOLUTIVO  
		ON DEVOLUTIVO.ESTADO = ESTADODEVOLUTIVO.ESTADO 
LEFT JOIN D_MOVIMIENTO 
ON DEVOLUTIVO.COMPANIA = D_MOVIMIENTO.COMPANIA 
AND DEVOLUTIVO.ELEMENTO = D_MOVIMIENTO.ELEMENTO 
AND DEVOLUTIVO.SERIE = D_MOVIMIENTO.SERIE

LEFT JOIN TIPOMOVIMIENTO  
		ON  DEVOLUTIVO.COMPANIA         = TIPOMOVIMIENTO.COMPANIA  
		AND DEVOLUTIVO.TIPOMOVIMIENTOF  = TIPOMOVIMIENTO.CODIGO         
WHERE 
DEVOLUTIVO.COMPANIA = 's$compania$s'
AND DEVOLUTIVO.DEPENDENCIA = '000000000000'
AND DEVOLUTIVO.ELEMENTO BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s' 
s$devolutivo$s 
s$comodato$s
s$consolidado$s

GROUP BY DEVOLUTIVO.COMPANIA, 
DEVOLUTIVO.ELEMENTO, 
DEVOLUTIVO.SERIE, 
TRIM(INVENTARIO.NOMBRELARGO)||' -'||DEVOLUTIVO.DESCRIPCION, 
PCK_GENERALES.FC_NOMBREINVENTARIO(SUBSTR(DEVOLUTIVO.ELEMENTO,PCK_SYSMAN_UTL.FC_PAR(DEVOLUTIVO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',PCK_DATOS.FC_MODULOALMACEN,SYSDATE)),DEVOLUTIVO.COMPANIA,NULL), 
INVENTARIO.NOMBRELARGO, ' -', DEVOLUTIVO.DESCRIPCION, SUBSTR(DEVOLUTIVO.ELEMENTO,PCK_SYSMAN_UTL.FC_PAR(DEVOLUTIVO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',PCK_DATOS.FC_MODULOALMACEN,SYSDATE)), 
DEVOLUTIVO.SERIEDEVOLUTIVO, 
ESTADODEVOLUTIVO.DESCRIPCION, 
DEVOLUTIVO.VALOR, 
INVENTARIO.TIPO,
DEVOLUTIVO.PLACAANULADA
ORDER BY 
 DEVOLUTIVO.SERIE