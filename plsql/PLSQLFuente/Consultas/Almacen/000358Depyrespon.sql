SELECT  DEPENDENCIA_RESPONSABLE.DEPENDENCIA, 
        RESPONSABLE.CEDULA, 
        TERCERO.NOMBRE NOMBRETERCERO, 
        RESPONSABLE.CARGO, 
        DEPENDENCIA.NOMBRE NOMBREDEPENDENCIA, 
        CASE DEPENDENCIA_RESPONSABLE.RESPONSABLEALMACEN WHEN 0 THEN ' ' ELSE 'X' END RESPONSABLEALMACEN
FROM    RESPONSABLE 
        INNER JOIN TERCERO
        ON  RESPONSABLE.COMPANIA = TERCERO.COMPANIA
        AND RESPONSABLE.CEDULA = TERCERO.NIT
        AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
        INNER JOIN DEPENDENCIA_RESPONSABLE 
        ON  RESPONSABLE.COMPANIA = DEPENDENCIA_RESPONSABLE.COMPANIA
        AND RESPONSABLE.CEDULA = DEPENDENCIA_RESPONSABLE.RESPONSABLE
        AND RESPONSABLE.SUCURSAL = DEPENDENCIA_RESPONSABLE.SUCURSAL
        INNER JOIN DEPENDENCIA
        ON  DEPENDENCIA.COMPANIA = DEPENDENCIA_RESPONSABLE.COMPANIA
        AND DEPENDENCIA.CODIGO = DEPENDENCIA_RESPONSABLE.DEPENDENCIA
WHERE   DEPENDENCIA.COMPANIA = s$compania$s  
 AND    DEPENDENCIA.CODIGO >= 's$depenInicial$s'
 AND    DEPENDENCIA.CODIGO <= 's$depenFinal$s'
ORDER BY  
        DEPENDENCIA_RESPONSABLE.DEPENDENCIA,
        TERCERO.NOMBRE
