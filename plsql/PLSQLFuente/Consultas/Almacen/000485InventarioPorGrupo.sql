SELECT SUBSTR(D_MOVIMIENTO.ELEMENTO,1,s$parametroDigitos$s) AS GRUPO,  
        INVENTARIO.NOMBRELARGO AS NOMBRE, 
        SUM(CASE WHEN V_MOVIMIENTO.CLASE = 'E'
              THEN D_MOVIMIENTO.CANTIDAD
              ELSE D_MOVIMIENTO.CANTIDAD * (-1)
            END) AS SALCANTIDAD,
        TRUNC( (CASE WHEN SUM(CASE WHEN V_MOVIMIENTO.CLASE = 'E'
                              THEN D_MOVIMIENTO.CANTIDAD
                              ELSE D_MOVIMIENTO.CANTIDAD * (-1)
                          END ) = 0 
                  THEN 0
                  ELSE SUM(CASE WHEN V_MOVIMIENTO.CLASE = 'E'
                              THEN D_MOVIMIENTO.VALORTOTAL
                              ELSE D_MOVIMIENTO.VALORTOTAL * -1
                            END)
                  END
              )*100+0.501)/100 AS SALVALOR 
 FROM   INVENTARIO   
 INNER  JOIN D_MOVIMIENTO ON (INVENTARIO.COMPANIA = D_MOVIMIENTO.COMPANIA)  
 INNER JOIN V_MOVIMIENTO ON V_MOVIMIENTO.COMPANIA= D_MOVIMIENTO.COMPANIA 
                       AND V_MOVIMIENTO.TIPOMOVIMIENTO= D_MOVIMIENTO.TIPOMOVIMIENTO 
                       AND V_MOVIMIENTO.NUMERO= D_MOVIMIENTO.MOVIMIENTO 
 WHERE  D_MOVIMIENTO.COMPANIA = s$compania$s  
 AND    INVENTARIO.CODIGOELEMENTO=SUBSTR(D_MOVIMIENTO.ELEMENTO,1,s$parametroDigitos$s)  
 AND    D_MOVIMIENTO.IND_REG NOT IN(0)  
 AND    D_MOVIMIENTO.FECHA <= s$fecha$s 
 AND    SUBSTR(D_MOVIMIENTO.ELEMENTO,1,s$parametroDigitos$s) BETWEEN 's$elementoDesde$s' AND 's$elementoHasta$s' 
 GROUP  BY SUBSTR(D_MOVIMIENTO.ELEMENTO,1,s$parametroDigitos$s),  INVENTARIO.NOMBRELARGO 
 ORDER  BY SUBSTR(D_MOVIMIENTO.ELEMENTO,1,s$parametroDigitos$s)
