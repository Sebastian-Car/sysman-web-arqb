MERGE INTO CONSULTAS FIN USING (SELECT '001862DevolutivosPorGrupoAc' INFORME ,TO_CLOB(q'[WITH CORRECCION_VALOR AS (
SELECT D_MOVIMIENTO.COMPANIA, D_MOVIMIENTO.ELEMENTO, D_MOVIMIENTO.SERIE, SUM (D_MOVIMIENTO.VALORTOTAL) VALORTOTAL
    FROM D_MOVIMIENTO
    INNER JOIN TIPOMOVIMIENTO
    ON D_MOVIMIENTO.COMPANIA = TIPOMOVIMIENTO.COMPANIA
    AND D_MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
    WHERE D_MOVIMIENTO.COMPANIA = s$compania$s
    AND TIPOMOVIMIENTO.CLASE = 'E'
    AND TIPOMOVIMIENTO.CONCEPTO = 'CR'
    AND D_MOVIMIENTO.FECHA >= s$fechadeCorte$s
    GROUP BY D_MOVIMIENTO.COMPANIA, D_MOVIMIENTO.ELEMENTO, D_MOVIMIENTO.SERIE)
SELECT
    DEVOLUTIVO.COMPANIA,
    INVENTARIO.CODIGOELEMENTO,
    INVENTARIO.NOMBRELARGO,
    DEVOLUTIVO.ELEMENTO CODIGOELEMENTODEV,
    DEVOLUTIVO.SERIE,
    DEVOLUTIVO.DESCRIPCION,
    DEVOLUTIVO.MARCA,
    DEVOLUTIVO.SERIEDEVOLUTIVO,
    DEPENDENCIA.NOMBRE,
    NVL(TERCERO.NOMBRE1,'')
    || ' '
    || NVL(TERCERO.NOMBRE2,'')
    || ' '
    || NVL(TERCERO.APELLIDO1,'')
    || ' '
    || NVL(TERCERO.APELLIDO2,'') NOMBRECOMPLETO,
    TRIM(INVENTARIO.NOMBRELARGO)
    || '-'
    || DEVOLUTIVO.DESCRIPCION  NOMBRELARGOS,
    (DEVOLUTIVO.VALOR - NVL(CORRECCION_VALOR.VALORTOTAL,0))  VALORDEVOLUTIVO,
    SUBSTR(INVENTARIO.CODIGOELEMENTO,0,s$digitosagrupacioninventario$s)  AGRUPACION,
    DEVOLUTIVO.DEPENDENCIA,
    CASE WHEN TO_CHAR(DEVOLUTIVO.PLACAANULADA) = '-1'
    THEN 'X' ELSE ' ' END PLACA,
    NVL(TO_CHAR(DEVOLUTIVO.FECHAULTMOV,'DD/MM/YYYY'),TO_CHAR(FECHAADQUISICION,'DD/MM/YYYY')) SALIDAF,
    NVL(TO_CHAR(DEVOLUTIVO.FECHAANULADA,'DD/MM/YYYY'),TO_CHAR(FECHAADQUISICION,'DD/MM/YYYY')) ANULADA,
    ESTADODEVOLUTIVO.DESCRIPCION ESTADO
FROM
    DEVOLUTIVO
    INNER JOIN INVENTARIO 
        ON DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA
        AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
    INNER JOIN DEPENDENCIA 
        ON ( DEVOLUTIVO.COMPANIA = DEPENDENCIA.COMPANIA )
        AND ( DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO )
    INNER JOIN RESPONSABLE 
        ON DEVOLUTIVO.COMPANIA = RESPONSABLE.COMPANIA
        AND DEVOLUTIVO.RESPONSABLE = RESPONSABLE.CEDULA
    INNER JOIN TERCERO 
        ON RESPONSABLE.COMPANIA = TERCERO.COMPANIA
        AND RESPONSABLE.CEDULA = TERCERO.NIT
        AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
    LEFT JOIN ESTADODEVOLUTIVO 
        ON DEVOLUTIVO.ESTADO = ESTADODEVOLUTIVO.ESTADO
    LEFT JOIN CORRECCION_VALOR
        ON DEVOLUTIVO.COMPANIA = CORRECCION_VALOR.COMPANIA
        AND DEVOLUTIVO.ELEMENTO = CORRECCION_VALOR.ELEMENTO
        AND DEVOLUTIVO.SERIE = CORRECCION_VALOR.SERIE
WHERE
    DEVOLUTIVO.COMPANIA = s$compania$s
		AND   NVL(DEVOLUTIVO.FECHAANULADA,FECHAADQUISICION) <= s$fechadeCorte$s
		AND   SUBSTR(INVENTARIO.CODIGOELEMENTO,0,5) BETWEEN 's$elementoinicial$s' AND 's$elementofinal$s'
	ORDER BY DEVOLUTIVO.SERIE]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[Nota. Es necesaria para el reporte]') CONSULTA_OPCIONAL, 'jrojas' CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);