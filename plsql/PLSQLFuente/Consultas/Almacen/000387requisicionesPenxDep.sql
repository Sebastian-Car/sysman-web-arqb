MERGE INTO CONSULTAS FIN USING (SELECT '000387requisicionesPenxDep' INFORME ,TO_CLOB(q'[SELECT DISTINCT 
ORDENDESUMINISTRO.NUMERO, 
TO_CHAR(ORDENDESUMINISTRO.FECHA,'DD/MM/YYYY') FECHA, 
DEPENDENCIA.CODIGO, 
DEPENDENCIA.NOMBRE DEPENDENCIA_NOMBRE, 
TERCERO.NOMBRE RESPONSABLE_NOMBRE
FROM ORDENDESUMINISTRO LEFT JOIN DEPENDENCIA 
    ON ORDENDESUMINISTRO.DEPENDENCIA = DEPENDENCIA.CODIGO 
    AND ORDENDESUMINISTRO.COMPANIA = DEPENDENCIA.COMPANIA 
LEFT JOIN RESPONSABLE 
ON ORDENDESUMINISTRO.TERCERO = RESPONSABLE.CEDULA 
AND ORDENDESUMINISTRO.COMPANIA = RESPONSABLE.COMPANIA
INNER JOIN TERCERO
    ON  RESPONSABLE.COMPANIA=TERCERO.COMPANIA
    AND RESPONSABLE.SUCURSAL= TERCERO.SUCURSAL
    AND   RESPONSABLE.CEDULA=TERCERO.NIT 
LEFT JOIN D_ORDENDECOMPRA 
ON ORDENDESUMINISTRO.COMPANIA = D_ORDENDECOMPRA.COMPANIA 
AND ORDENDESUMINISTRO.NUMERO = D_ORDENDECOMPRA.ORDENDESUMINISTRO 
LEFT JOIN D_ORDENDESUMINISTRO 
ON ORDENDESUMINISTRO.COMPANIA = D_ORDENDESUMINISTRO.COMPANIA 
AND ORDENDESUMINISTRO.NUMERO = D_ORDENDESUMINISTRO.ORDENDESUMINISTRO 
LEFT JOIN MOVIMIENTO 
ON ORDENDESUMINISTRO.COMPANIA = MOVIMIENTO.COMPANIA 
AND ORDENDESUMINISTRO.NUMERO = MOVIMIENTO.MOVASOCIADO

LEFT JOIN TIPOMOVIMIENTO 
ON MOVIMIENTO.COMPANIA = TIPOMOVIMIENTO.COMPANIA 
AND MOVIMIENTO.TIPOMOVIMIENTO= TIPOMOVIMIENTO.CODIGO
WHERE  ORDENDESUMINISTRO.COMPANIA = s$compania$s
      AND ORDENDESUMINISTRO.FECHA BETWEEN   s$fecha_inicial$s AND  s$fecha_final$s
      AND D_ORDENDESUMINISTRO.ELEMENTO BETWEEN   's$elemento_inicio$s' AND   's$elemento_fin$s' 
      AND DEPENDENCIA.CODIGO BETWEEN   's$dependencia_inicio$s' AND   's$dependencia_fin$s' 
      AND D_ORDENDECOMPRA.ORDENDESUMINISTRO IS NULL 
     AND TIPOMOVIMIENTO.CLASE='E'
     AND MOVIMIENTO.CONCEPTO='C'
      /*AND (MOVIMIENTO.TIPOMOVASOCIADO='R' OR MOVIMIENTO.TIPOMOVASOCIADO IS NULL)
      AND MOVIMIENTO.MOVASOCIADO IS NULL  */]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
      
      COMMIT;