MERGE INTO CONSULTAS FIN USING (SELECT '000617IInventarioSuministroFechas' INFORME ,TO_CLOB(q'[SELECT
	V_BASE_CUENTAALMACEN.GRUPO,
	V_BASE_CUENTAALMACEN.NOMBREGRUPO NOMBREINVENTARIO,
	V_BASE_CUENTAALMACEN.ELEMENTO,
	V_BASE_CUENTAALMACEN.NOMBREELEMENTO NOMBRELARGO,
	NVL(V_BASE_CUENTAALMACEN.UNIDAD , ' ')UNIDAD,
	SUM(NVL(V_BASE_CUENTAALMACEN.ENTRADAS, 0)-NVL(V_BASE_CUENTAALMACEN.SALIDAS, 0)+ NVL(V_BASE_CUENTAALMACEN.CANTIDADAJUSTESDEBITO, 0)+ NVL(V_BASE_CUENTAALMACEN.CANTIDADAJUSTESCREDITO, 0)) AS SUMADECANTIDADSALDONUEVO,
	 SUM(NVL(V_BASE_CUENTAALMACEN.VALOR_ENTRADAS,0)-NVL(V_BASE_CUENTAALMACEN.VALOR_SALIDAS,0) + 
            NVL(V_BASE_CUENTAALMACEN.AJUSTESDEBITO,0) +NVL(V_BASE_CUENTAALMACEN.AJUSTESCREDITO,0))AS SUMADESALDOFINAL
FROM
	V_BASE_CUENTAALMACEN
LEFT JOIN (
	SELECT
		D_MOVIMIENTO.COMPANIA,
		D_MOVIMIENTO.ELEMENTO,
		MAX(D_MOVIMIENTO.TIPOMOVIMIENTO) KEEP (DENSE_RANK LAST
	ORDER BY
		D_MOVIMIENTO.FECHA,
		D_MOVIMIENTO.HORA) AS ULTIMODETIPOMOVIMIENTO,
		MAX(D_MOVIMIENTO.MOVIMIENTO) KEEP (DENSE_RANK LAST
	ORDER BY
		D_MOVIMIENTO.FECHA,
		D_MOVIMIENTO.HORA) AS ÚLTIMODEMOVIMIENTO,
		MAX(D_MOVIMIENTO.ESTADO) KEEP (DENSE_RANK LAST
	ORDER BY
		D_MOVIMIENTO.FECHA,
		D_MOVIMIENTO.HORA) AS ÚLTIMODEESTADO,
		MAX(TO_CHAR(D_MOVIMIENTO.FECHA, 'YYYYMMDD')) KEEP (DENSE_RANK LAST
	ORDER BY
		D_MOVIMIENTO.FECHA,
		D_MOVIMIENTO.HORA) AS FECHAX,
		MAX(D_MOVIMIENTO.FECHA) KEEP (DENSE_RANK LAST
	ORDER BY
		D_MOVIMIENTO.FECHA,
		D_MOVIMIENTO.HORA) AS ÚLTIMODEFECHA,
		MAX(D_MOVIMIENTO.SALDOKARDEX) KEEP (DENSE_RANK LAST
	ORDER BY
		D_MOVIMIENTO.FECHA,
		D_MOVIMIENTO.HORA) AS CSALDO,
		MAX(D_MOVIMIENTO.VALORSALDO) KEEP (DENSE_RANK LAST
	ORDER BY
		D_MOVIMIENTO.FECHA,
		D_MOVIMIENTO.HORA) AS VSALDO,
		MAX(D_MOVIMIENTO.VALORUNITARIO) KEEP (DENSE_RANK LAST
	ORDER BY
		D_MOVIMIENTO.FECHA,
		D_MOVIMIENTO.HORA) AS VPROMEDIO
	FROM
		D_Movimiento
	INNER JOIN V_MOVIMIENTO MOVIMIENTO ON
		D_Movimiento.COMPANIA = MOVIMIENTO.COMPANIA
		AND 
	 D_Movimiento.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO
		AND 
	 D_Movimiento.MOVIMIENTO = MOVIMIENTO.NUMERO
	WHERE
		D_MOVIMIENTO.COMPANIA = s$compania$s
		AND D_MOVIMIENTO.FECHA <= s$fechaFinal$s
		AND D_MOVIMIENTO.IND_REG NOT IN (0)
	GROUP BY
		D_MOVIMIENTO.COMPANIA,
		D_MOVIMIENTO.ELEMENTO) ULTIMOMOVIMIENTOC_P ON
	V_BASE_CUENTAALMACEN.COMPANIA = ULTIMOMOVIMIENTOC_P.COMPANIA
	AND 
                V_BASE_CUENTAALMACEN.ELEMENTO = ULTIMOMOVIMIENTOC_P.ELEMENTO
WHERE
	V_BASE_CUENTAALMACEN.COMPANIA = s$compania$s
	AND TO_CHAR(V_BASE_CUENTAALMACEN.FECHA, 'DD/MM/YYYY') <= s$fechaFinal$s
	AND V_BASE_CUENTAALMACEN.IND_REG NOT IN (0)
	AND V_BASE_CUENTAALMACEN.BODEGA_DESTINO1 = 20
GROUP BY
	V_BASE_CUENTAALMACEN.GRUPO,
	V_BASE_CUENTAALMACEN.NOMBREGRUPO,
	V_BASE_CUENTAALMACEN.ELEMENTO,
	V_BASE_CUENTAALMACEN.NOMBREELEMENTO,
	V_BASE_CUENTAALMACEN.UNIDAD,
	ULTIMOMOVIMIENTOC_P.VSALDO
HAVING
	SUM(NVL(V_BASE_CUENTAALMACEN.ENTRADAS, 0)-NVL(V_BASE_CUENTAALMACEN.SALIDAS, 0)+ NVL(V_BASE_CUENTAALMACEN.CANTIDADAJUSTESDEBITO, 0)+ NVL(V_BASE_CUENTAALMACEN.CANTIDADAJUSTESCREDITO, 0)) NOT IN (0)]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);