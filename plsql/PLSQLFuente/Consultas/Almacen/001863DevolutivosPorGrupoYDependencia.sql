MERGE INTO CONSULTAS FIN USING (SELECT '001863DevolutivosPorGrupoYDependencia' INFORME ,TO_CLOB(q'[SELECT DEVOLUTIVO.COMPANIA,
  SUBSTR(INVENTARIO.CODIGOELEMENTO,0,s$digitosagrupacioninventario$s) AS AGRUPACION,
  DEVOLUTIVO.SERIE,
  DEVOLUTIVO.ELEMENTO CODIGOELEMENTODEV,
  DEVOLUTIVO.DEPENDENCIA,
  DEVOLUTIVO.RESPONSABLE,
  TRIM(INVENTARIO.NOMBRELARGO)    || '-'     || DEVOLUTIVO.DESCRIPCION NOMBRELARGO,
  DEVOLUTIVO.MARCA,
  DEPENDENCIA.CODIGO,
  DEPENDENCIA.NOMBRE,
  DEVOLUTIVO.SERIEDEVOLUTIVO,
  ESTADODEVOLUTIVO.DESCRIPCION ESTADO,
  I.NOMBRELARGO NOMBREGR,
  TERCERO.NOMBRE NOMBRECOMPLETO,
  DEVOLUTIVO.VALOR VALORDEVOLUTIVO
FROM DEVOLUTIVO
INNER JOIN INVENTARIO
ON DEVOLUTIVO.COMPANIA  = INVENTARIO.COMPANIA
AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
INNER JOIN INVENTARIO I
ON DEVOLUTIVO.COMPANIA              = I.COMPANIA
AND SUBSTR(DEVOLUTIVO.ELEMENTO,0,s$digitosagrupacioninventario$s) = I.CODIGOELEMENTO
INNER JOIN DEPENDENCIA
ON DEVOLUTIVO.COMPANIA     = DEPENDENCIA.COMPANIA
AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO
INNER JOIN RESPONSABLE
ON DEVOLUTIVO.COMPANIA     = RESPONSABLE.COMPANIA
AND DEVOLUTIVO.RESPONSABLE = RESPONSABLE.CEDULA
INNER JOIN TERCERO
ON RESPONSABLE.COMPANIA  = TERCERO.COMPANIA
AND RESPONSABLE.CEDULA   = TERCERO.NIT
AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
INNER JOIN ESTADODEVOLUTIVO
ON DEVOLUTIVO.ESTADO      = ESTADODEVOLUTIVO.ESTADO
WHERE DEVOLUTIVO.COMPANIA = s$compania$s  
AND SUBSTR(INVENTARIO.CODIGOELEMENTO,0,s$digitosagrupacioninventario$s) BETWEEN s$cmbelementodesde$s AND s$cmbelementohasta$s
AND DEVOLUTIVO.DEPENDENCIA BETWEEN 's$cmbcodigoinicial$s' AND 's$cmbcodigofinal$s'
AND DEVOLUTIVO.PLACAANULADA IN (0)
ORDER BY INVENTARIO.NOMBRELARGO]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
