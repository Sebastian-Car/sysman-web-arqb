SELECT 
  TIPOMOVIMIENTO.NOMBRE,  
  MOVIMIENTO.NUMERO,  
  D_ORDENDECOMPRA.ELEMENTO  CODINVEN,  
  INVENTARIO.NOMBRELARGO,  
  TIPOORDENDECOMPRA.NOMBRE || ' - ' || D_ORDENDECOMPRA.ORDENDECOMPRA  PROCESOCONTRACTUAL,  
  TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY')  FECHADECOMPRA,  
  D_ORDENDECOMPRA.CANTIDAD,  
  D_ORDENDECOMPRA.VALORUNITARIO  
FROM MOVIMIENTO INNER JOIN TIPOMOVIMIENTO  
            ON  MOVIMIENTO.COMPANIA = TIPOMOVIMIENTO.COMPANIA 
            AND MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO 
      INNER JOIN D_ORDENDECOMPRA  
            ON  MOVIMIENTO.COMPANIA = D_ORDENDECOMPRA.COMPANIA 
            AND MOVIMIENTO.TIPOMOVASOCIADO = D_ORDENDECOMPRA.CLASEORDEN 
            AND MOVIMIENTO.MOVASOCIADO = D_ORDENDECOMPRA.ORDENDECOMPRA 
      LEFT JOIN TIPOORDENDECOMPRA  
            ON  D_ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA 
            AND D_ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO 
      LEFT JOIN ORDENDECOMPRA  
            ON  D_ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRA.CLASEORDEN 
            AND D_ORDENDECOMPRA.COMPANIA = ORDENDECOMPRA.COMPANIA 
            AND D_ORDENDECOMPRA.ORDENDECOMPRA = ORDENDECOMPRA.NUMERO 
      LEFT JOIN INVENTARIO  
            ON  D_ORDENDECOMPRA.COMPANIA = INVENTARIO.COMPANIA 
            AND D_ORDENDECOMPRA.ELEMENTO = INVENTARIO.CODIGOELEMENTO 
WHERE D_ORDENDECOMPRA.COMPANIA =s$compania$s 
  AND TIPOMOVIMIENTO.CLASE = 'E'  
  AND TIPOMOVIMIENTO.CONCEPTO IN('C', 'CM', 'II', 'P', 'RM', 'T') 
  AND D_ORDENDECOMPRA.ELEMENTO BETWEEN 's$desde$s' AND 's$hasta$s'   
ORDER BY D_ORDENDECOMPRA.ELEMENTO