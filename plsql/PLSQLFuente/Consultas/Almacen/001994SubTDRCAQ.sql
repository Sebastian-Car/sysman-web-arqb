SELECT DISTINCT
  D_MOVIMIENTO.COMPANIA,
  D_MOVIMIENTO.TIPOMOVIMIENTO,
  D_MOVIMIENTO.MOVIMIENTO,
  SUM(D_MOVIMIENTO.VALORTOTAL)    SUMADEVALORTOTAL,
  SUBSTR (D_MOVIMIENTO.ELEMENTO,1,3) GRUPO 
FROM D_MOVIMIENTO
WHERE D_MOVIMIENTO.COMPANIA       = '$P!{PR_COMPANIA}'
  AND D_MOVIMIENTO.TIPOMOVIMIENTO = '$P!{PR_TIPOMOVIMIENTO}'
  AND D_MOVIMIENTO.MOVIMIENTO BETWEEN $P!{PR_MOVIMIENTO} AND $P!{PR_MOVIMIENTO}  
GROUP BY 
  D_MOVIMIENTO.COMPANIA,
  D_MOVIMIENTO.TIPOMOVIMIENTO,
  D_MOVIMIENTO.MOVIMIENTO,
  SUBSTR (D_MOVIMIENTO.ELEMENTO,1,3)  
ORDER BY GRUPO  
