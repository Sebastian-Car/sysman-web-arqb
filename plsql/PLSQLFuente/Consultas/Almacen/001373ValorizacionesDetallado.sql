SELECT  VALORIZACIONDEVOLUTIVO.SERIE,
        VALORIZACIONDEVOLUTIVO.FECHAAVALUO,
        VALORIZACIONDEVOLUTIVO.FECHAVALORIZACION,
        VALORIZACIONDEVOLUTIVO.DESCRIPCION,
        VALORIZACIONDEVOLUTIVO.AVALUO,
        VALORIZACIONDEVOLUTIVO.VALORIZACION,
        VALORIZACIONDEVOLUTIVO.ELEMENTO,
        INVENTARIO.NOMBRECORTO s$adicionSelect$s
FROM INVENTARIO
  INNER JOIN VALORIZACIONDEVOLUTIVO
     ON INVENTARIO.CODIGOELEMENTO           = VALORIZACIONDEVOLUTIVO.ELEMENTO
    AND INVENTARIO.COMPANIA                       = VALORIZACIONDEVOLUTIVO.COMPANIA
WHERE VALORIZACIONDEVOLUTIVO.COMPANIA       = s$compania$s
  AND VALORIZACIONDEVOLUTIVO.ELEMENTO BETWEEN 's$elementoInicial$s'    AND 's$elementoFinal$s'
  AND VALORIZACIONDEVOLUTIVO.SERIE        BETWEEN TRIM(s$placaInicial$s) AND TRIM(s$placaFinal$s)
  AND VALORIZACIONDEVOLUTIVO.PERIODO  BETWEEN s$fechaInicial$s            AND s$fechaFin$s
