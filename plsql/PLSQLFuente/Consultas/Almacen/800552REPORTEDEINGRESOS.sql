M.TIPOMOVIMIENTO TIPO_DE_MOVIMIENTO,
M.NUMERO NUMERO_DE_MOVIMIENTO,
M.FECHA FECHA_DEL_MOVIMIENTO,
T.NIT NIT_DEL_PROVEEDOR,
