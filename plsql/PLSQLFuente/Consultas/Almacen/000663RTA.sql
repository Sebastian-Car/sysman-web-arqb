SELECT D_CAMBIOS_TIPOACTIVO.COMPANIA,
  D_CAMBIOS_TIPOACTIVO.NUMERO,
  D_CAMBIOS_TIPOACTIVO.TIPOMOVIMIENTO,
  D_CAMBIOS_TIPOACTIVO.ELEMENTO,
  D_CAMBIOS_TIPOACTIVO.SERIE,
  D_CAMBIOS_TIPOACTIVO.TIPOACTIVO_ANT,
  D_CAMBIOS_TIPOACTIVO.TIPOACTIVO_NUE,
  NVL(INVENTARIO.NOMBRELARGO, ' ') NOMBRELARGO,
  CAMBIOS_TIPOACTIVO.DESCRIPCION DESCRIPCION_TRASLADO,
  TIPOMOVIMIENTO.NOMBRE,
  INVENTARIO.UNIDAD,
  CAMBIOS_TIPOACTIVO.CONSECUTIVO,
  CAMBIOS_TIPOACTIVO.TIPOMOVIMIENTO,
  NVL(INVENTARIO.DESCRIPCION,' ') DESCRIPCION,
  TIPO_ACTIVO.NOMBRE_TIPOACTIVO   TIPO_ANTERIOR,
  TIPO_ACTIVO_NUE.NOMBRE_TIPOACTIVO TIPO_NUEVO,
  D_CAMBIOS_TIPOACTIVO.REVELACIONES
FROM CAMBIOS_TIPOACTIVO
  LEFT JOIN D_CAMBIOS_TIPOACTIVO
    ON CAMBIOS_TIPOACTIVO.COMPANIA        = D_CAMBIOS_TIPOACTIVO.COMPANIA
    AND CAMBIOS_TIPOACTIVO.CONSECUTIVO    = D_CAMBIOS_TIPOACTIVO.NUMERO
    AND CAMBIOS_TIPOACTIVO.TIPOMOVIMIENTO = D_CAMBIOS_TIPOACTIVO.TIPOMOVIMIENTO
  LEFT JOIN DEVOLUTIVO
    ON D_CAMBIOS_TIPOACTIVO.COMPANIA  = DEVOLUTIVO.COMPANIA
    AND D_CAMBIOS_TIPOACTIVO.ELEMENTO = DEVOLUTIVO.ELEMENTO
    AND D_CAMBIOS_TIPOACTIVO.SERIE    = DEVOLUTIVO.SERIE
  LEFT JOIN INVENTARIO
    ON DEVOLUTIVO.COMPANIA  = INVENTARIO.COMPANIA
    AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
  LEFT JOIN TIPO_ACTIVO TIPO_ACTIVO_NUE
    ON D_CAMBIOS_TIPOACTIVO.COMPANIA        = TIPO_ACTIVO_NUE.COMPANIA
    AND D_CAMBIOS_TIPOACTIVO.TIPOACTIVO_NUE = TIPO_ACTIVO_NUE.CODIGO_TIPOACTIVO
  LEFT JOIN TIPOMOVIMIENTO
    ON CAMBIOS_TIPOACTIVO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
    AND CAMBIOS_TIPOACTIVO.COMPANIA      = TIPOMOVIMIENTO.COMPANIA
  LEFT JOIN TIPO_ACTIVO
    ON D_CAMBIOS_TIPOACTIVO.COMPANIA        = TIPO_ACTIVO.COMPANIA
    AND D_CAMBIOS_TIPOACTIVO.TIPOACTIVO_ANT = TIPO_ACTIVO.CODIGO_TIPOACTIVO
WHERE CAMBIOS_TIPOACTIVO.COMPANIA     = s$compania$s 
AND CAMBIOS_TIPOACTIVO.TIPOMOVIMIENTO =  's$tipoMovimiento$s'
AND CAMBIOS_TIPOACTIVO.CONSECUTIVO    BETWEEN s$movimientoInicial$s AND s$movimientoFinal$s
