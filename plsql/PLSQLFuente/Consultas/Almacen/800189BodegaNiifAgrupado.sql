WITH CONSULTAPRINCIPAL AS
  (SELECT DEPRECIAR.COMPANIA,
          DEPRECIAR.ELEMENTO,
          DEPRECIAR.SERIE,
          DEPRECIAR.PERIODO,
          INVENTARIO.NOMBRELARGO NOMBRELARGO,
          CASE
            WHEN TRIM(DEVOLUTIVO.MARCA) IS NULL
            THEN ' '
            ELSE 'Marca: ' || DEVOLUTIVO.MARCA
          END NOMBRELARGOS,
          INVENTARIO.UNIDAD,
          INVENTARIO.TIPO,
          DEPRECIAR.NIIF_COSTOAJUSTADO-NIIF_VLRELEMENTO AJUSTEXINFLACIONAC,
          DEPRECIAR.NIIF_ACUMDEPAJ ACUMDEPAJFER,
          DEPRECIAR.NIIF_VLRELEMENTO VLRELEMENTO,
          DEPRECIAR.NIIF_VLRDEPRECIACION VLRDEPRECIACION,
          DEPRECIAR.NIIF_VLRACUMULADO VLRACUMULADO,
          DEPRECIAR.NIIF_SALDOXDEPRECIAR SALDOXDEPRECIAR,
          DEPRECIAR.NIIF_VLRAJUSTADO VLRAJUSTADO,
          DEPRECIAR.NIIF_VLRACUMULADOAJ VLRACUMULADOAJ,
          DEPRECIAR.NIIF_COSTOAJUSTADO COSTOAJUSTADO,
          DEPRECIAR.NIIF_VLRLIBROS VLRLIBROS,
          DEPRECIAR.IMPRIMIR,
          DEPRECIAR.NIIF_VLRAJUSTEELEM VLRAJUSTEELEM,
          DEPRECIAR.NIIF_VLRACUMAJUSTEELEM VLRACUMAJUSTEELEM,
          DEPRECIAR.ELEMENTOBODEGA,
          DEPRECIAR.PLACAANULADA,
          DEPRECIAR.NIIF_VLRDEPRECIACIONAJ VLRDEPRECIACIONAJ,
          DEPRECIAR.NIIF_DEPACUMULADAAJ DEPACUMULADAAJ,
          DEPRECIAR.NIIF_SALDOAJ SALDOAJ,
          DEPRECIAR.NIIF_COSTOSALIDA COSTOSALIDA,
          DEPRECIAR.NIIF_COSTOSALIDAAJ COSTOSALIDAAJ,
          DEPRECIAR.NIIF_DEPACUMULADA DEPACUMULADA,
          DEPRECIAR.DEPENDENCIA,
          DEPRECIAR.NIIF_AJDEPACUMULADA AJDEPACUMULADA,
          DEPRECIAR.NIIF_AJDEPRECIACION AJDEPRECIACION,
          DEPRECIAR.ANO,
          DEPRECIAR.MES,
          DEPRECIAR.NIIF_ACUMDEPAJ ACUMDEPAJ,
          DEPRECIAR.NIIF_ACUMAJUSTESAJ ACUMAJUSTESAJ,
          DEVOLUTIVO.MARCA,
          DEVOLUTIVO.DESCRIPCION,
          DEVOLUTIVO.NIIF_FECHAADQUISICION FECHAADQUISICION,
          SUBSTR(DEPRECIAR.ELEMENTO,0, s$parametro$s) AGRUPACION,
          SUBSTR(DEPRECIAR.ELEMENTO,0,5) SUBGRP
    FROM DEPRECIAR
   INNER JOIN DEVOLUTIVO
      ON DEPRECIAR.COMPANIA = DEVOLUTIVO.COMPANIA 
     AND DEPRECIAR.ELEMENTO = DEVOLUTIVO.ELEMENTO
     AND DEPRECIAR.SERIE    = DEVOLUTIVO.SERIE
    LEFT JOIN INVENTARIO
      ON DEPRECIAR.COMPANIA   = INVENTARIO.COMPANIA
     AND DEPRECIAR.ELEMENTO    = INVENTARIO.CODIGOELEMENTO
   WHERE DEPRECIAR.COMPANIA = s$compania$s
     AND DEPRECIAR.ELEMENTO BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s'
     AND TO_CHAR(DEPRECIAR.PERIODO,'DD/MM/YYYY') = TO_DATE('s$ultimoDia$s','DD/MM/YYYY')
     AND (DEVOLUTIVO.FECHAANULADA IS NULL
      OR TO_CHAR(DEVOLUTIVO.FECHAANULADA,'DD/MM/YYYY')  > TO_DATE('s$ultimoDia$s','DD/MM/YYYY'))
     AND DEPRECIAR.BODEGA IN ('20')
 AND TO_CHAR(DEVOLUTIVO.NIIF_FECHAADQUISICION,'DD/MM/YYYY') <= TO_DATE('s$ultimoDia$s','DD/MM/YYYY')
    ORDER BY DEPRECIAR.COMPANIA,
             DEPRECIAR.ELEMENTO,
             DEPRECIAR.SERIE,
             DEPRECIAR.PERIODO)
SELECT CONSULTAPRINCIPAL.AGRUPACION, 
       CONSULTAPRINCIPAL.NOMBRELARGO  NOMBREGRUPO,  
       CONSULTAPRINCIPAL.PERIODO, 
       CONSULTAPRINCIPAL.UNIDAD, 
       SUM(CONSULTAPRINCIPAL.VLRELEMENTO) VALOR_H, 
       SUM(CONSULTAPRINCIPAL.COSTOAJUSTADO)  COSTO_AJUSTADO, 
       SUM(CONSULTAPRINCIPAL.VLRDEPRECIACION)  DEPRECIACION_PERIODO, 
       SUM(CONSULTAPRINCIPAL.DEPACUMULADA)  DEP_ACUMULADA, 
       SUM((CONSULTAPRINCIPAL.DEPACUMULADA-CONSULTAPRINCIPAL.VLRDEPRECIACION)) DEP_ANTERIOR,  
       SUM(CONSULTAPRINCIPAL.VLRLIBROS) VLRLIBROS,  
       NIIF_INVENTARIOCONTA.CUENTACREDITO CODIGOCUENTA, 
       PLAN_CONTABLE.NOMBRE  NOMBRECUENTA
  FROM CONSULTAPRINCIPAL
  LEFT JOIN NIIF_INVENTARIOCONTA
    ON CONSULTAPRINCIPAL.COMPANIA    = NIIF_INVENTARIOCONTA.COMPANIA
   AND CONSULTAPRINCIPAL.ANO        = NIIF_INVENTARIOCONTA.ANO
   AND CONSULTAPRINCIPAL.AGRUPACION = NIIF_INVENTARIOCONTA.CODIGOELEMENTO
  LEFT JOIN PLAN_CONTABLE
    ON NIIF_INVENTARIOCONTA.COMPANIA = PLAN_CONTABLE.COMPANIA
   AND NIIF_INVENTARIOCONTA.ANO     = PLAN_CONTABLE.ANO
   AND NIIF_INVENTARIOCONTA.CUENTACREDITO = PLAN_CONTABLE.CODIGO
  GROUP BY CONSULTAPRINCIPAL.AGRUPACION, 
       CONSULTAPRINCIPAL.NOMBRELARGO ,  
       CONSULTAPRINCIPAL.PERIODO, 
       CONSULTAPRINCIPAL.UNIDAD,
       NIIF_INVENTARIOCONTA.CUENTACREDITO, 
       PLAN_CONTABLE.NOMBRE
