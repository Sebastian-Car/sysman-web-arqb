SELECT DEPENDENCIA.NOMBRE NOMBRE_DEPENDENCIA, 
TERCERO.NOMBRE NOMBRE_RESPONSABLE, 
RESPONSABLE.CEDULA, 
RESPONSABLE.CARGO, 
DEPENDENCIA_RESPONSABLE.RESPONSABLE,
NVL(PCK_SYSMAN_UTL.FC_PAR(DEPENDENCIA_RESPONSABLE.COMPANIA, 'ENCABEZADO PAZ Y SALVO', PCK_DATOS.FC_MODULOALMACEN, SYSDATE), ' ') ENCABEZADO,
NVL(PCK_SYSMAN_UTL.FC_PAR(DEPENDENCIA_RESPONSABLE.COMPANIA, 'ENCARGADO DE LOS BIENES', PCK_DATOS.FC_MODULOALMACEN, SYSDATE), ' ') ENCARGADO,
NVL(PCK_SYSMAN_UTL.FC_PAR(DEPENDENCIA_RESPONSABLE.COMPANIA, 'NOMBRE DEL PROFESIONAL UNIVERSITARIO', PCK_DATOS.FC_MODULOALMACEN, SYSDATE),' ') NOMBRE_PROFESIONAL,
NVL(PCK_SYSMAN_UTL.FC_PAR(DEPENDENCIA_RESPONSABLE.COMPANIA, 'CARGO DEL PROFESIONAL UNIVERSITARIO', PCK_DATOS.FC_MODULOALMACEN, SYSDATE), ' ') CARGO_PROFESIONAL
FROM DEPENDENCIA_RESPONSABLE 
  LEFT JOIN DEPENDENCIA ON DEPENDENCIA_RESPONSABLE.COMPANIA = DEPENDENCIA.COMPANIA
    AND DEPENDENCIA_RESPONSABLE.DEPENDENCIA = DEPENDENCIA.CODIGO
  LEFT JOIN RESPONSABLE ON DEPENDENCIA_RESPONSABLE.COMPANIA = RESPONSABLE.COMPANIA
    AND DEPENDENCIA_RESPONSABLE.RESPONSABLE = RESPONSABLE.CEDULA
    AND DEPENDENCIA_RESPONSABLE.SUCURSAL = RESPONSABLE.SUCURSAL
  INNER JOIN TERCERO ON RESPONSABLE.COMPANIA = TERCERO.COMPANIA
    AND RESPONSABLE.CEDULA = TERCERO.NIT
    AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
WHERE DEPENDENCIA_RESPONSABLE.COMPANIA = s$compania$s
 AND DEPENDENCIA_RESPONSABLE.RESPONSABLE='s$reponsable$s'
