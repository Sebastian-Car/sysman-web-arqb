SELECT DISTINCT 
ORDENDESUMINISTRO.NUMERO, 
ORDENDESUMINISTRO.DEPENDENCIA, 
TO_CHAR(ORDENDESUMINISTRO.FECHA,'DD/MM/YYYY') FECHA, 
ORDENDESUMINISTRO.DESCRIPCION, 
D_ORDENDESUMINISTRO.CODIGO, 
D_ORDENDESUMINISTRO.ELEMENTO, 
D_ORDENDESUMINISTRO.ESPECIFICACION, 
D_ORDENDESUMINISTRO.CANTIDADPORENTREGAR, 
INVENTARIO.NOMBRELARGO, 
ORDENDESUMINISTRO.COMPANIA, 
DEPENDENCIA.NOMBRE  DEPENDENCIA_NOMBRE, 
TERCERO.NOMBRE RESPONSABLE_NOMBRE, 
INVENTARIO.UNIDAD,
UNIDAD.NOMBRE UNIDAD_NOMBRE
FROM RESPONSABLE INNER JOIN  DEPENDENCIA 
		INNER JOIN DEPENDENCIA_RESPONSABLE
			ON DEPENDENCIA.COMPANIA = DEPENDENCIA_RESPONSABLE.COMPANIA
	AND DEPENDENCIA.CODIGO = DEPENDENCIA_RESPONSABLE.DEPENDENCIA
	ON RESPONSABLE.COMPANIA = DEPENDENCIA_RESPONSABLE.COMPANIA
	AND RESPONSABLE.CEDULA = DEPENDENCIA_RESPONSABLE.RESPONSABLE
INNER JOIN TERCERO
	ON  RESPONSABLE.COMPANIA=TERCERO.COMPANIA
	AND RESPONSABLE.SUCURSAL= TERCERO.SUCURSAL
	AND   RESPONSABLE.CEDULA=TERCERO.NIT
INNER JOIN ORDENDESUMINISTRO 
	ON DEPENDENCIA_RESPONSABLE.COMPANIA = ORDENDESUMINISTRO.COMPANIA
	AND DEPENDENCIA_RESPONSABLE.DEPENDENCIA = ORDENDESUMINISTRO.DEPENDENCIA
	AND DEPENDENCIA_RESPONSABLE.RESPONSABLE = ORDENDESUMINISTRO.TERCERO
	and DEPENDENCIA_RESPONSABLE.SUCURSAL =ORDENDESUMINISTRO.SUCURSAL
INNER JOIN INVENTARIO LEFT JOIN UNIDAD 
	ON INVENTARIO.UNIDAD = UNIDAD.UNIDAD 
		INNER JOIN D_ORDENDESUMINISTRO 
		ON INVENTARIO.COMPANIA = D_ORDENDESUMINISTRO.COMPANIA
		AND INVENTARIO.CODIGOELEMENTO = D_ORDENDESUMINISTRO.ELEMENTO 
	ON ORDENDESUMINISTRO.COMPANIA = D_ORDENDESUMINISTRO.COMPANIA
	AND ORDENDESUMINISTRO.NUMERO = D_ORDENDESUMINISTRO.ORDENDESUMINISTRO