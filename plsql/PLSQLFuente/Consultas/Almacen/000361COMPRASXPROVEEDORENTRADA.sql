SELECT 
    TERCERO.NOMBRE  PROVEEDOR, 
    INVENTARIO.NOMBRELARGO, 
    D_MOVIMIENTO.CANTIDAD,
    MOVIMIENTO.TIPOMOVIMIENTO,
    D_MOVIMIENTO.MOVIMIENTO,
    TO_CHAR(MOVIMIENTO.FECHA,'DD/MM/YYYY') FECHA,
    D_MOVIMIENTO.VALORUNITARIO, 
    D_MOVIMIENTO.VALORTOTAL, 
    MOVIMIENTO.VALORTOTAL, 
    INVENTARIO.CODIGOELEMENTO 
FROM D_MOVIMIENTO 
INNER JOIN MOVIMIENTO 
ON D_MOVIMIENTO.COMPANIA = MOVIMIENTO.COMPANIA 
AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO 
AND D_MOVIMIENTO.MOVIMIENTO = MOVIMIENTO.NUMERO 
INNER JOIN TERCERO 
ON MOVIMIENTO.COMPANIA = TERCERO.COMPANIA 
AND MOVIMIENTO.TERCERO = TERCERO.NIT
AND MOVIMIENTO.SUCURSAL = TERCERO.SUCURSAL  
INNER JOIN INVENTARIO 
ON D_MOVIMIENTO.COMPANIA = INVENTARIO.COMPANIA 
AND D_MOVIMIENTO.ELEMENTO = INVENTARIO.CODIGOELEMENTO 
INNER JOIN TIPOMOVIMIENTO 
ON MOVIMIENTO.COMPANIA = TIPOMOVIMIENTO.COMPANIA
AND MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO   
WHERE D_MOVIMIENTO.COMPANIA =  s$compania$s
AND MOVIMIENTO.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
AND TERCERO.NIT BETWEEN 's$proveedorInicial$s' AND 's$proveedorFinal$s' 
AND TIPOMOVIMIENTO.CLASE = 'E'
AND TIPOMOVIMIENTO.CONCEPTO <> 'R'