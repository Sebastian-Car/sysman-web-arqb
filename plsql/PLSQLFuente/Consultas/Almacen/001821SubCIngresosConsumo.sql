WITH C_RESUMENINGEGR AS (SELECT CINGRESOSCONSUMO.COMPANIA,
       CINGRESOSCONSUMO.AGRUPACION,
       SUM(CINGRESOSCONSUMO.TOTAL) TOTAL
FROM
      (SELECT D_MOVIMIENTO.COMPANIA,
              D_MOVIMIENTO.TIPOMOVIMIENTO,
              TIPOMOVIMIENTO.CLASE,
              TIPOMOVIMIENTO.TIPOELEMENTO,
              SUM(D_MOVIMIENTO.VALORTOTAL) TOTAL,
              TIPOMOVIMIENTO.NOMBRECORTO,
              SUBSTR(D_MOVIMIENTO.ELEMENTO, 0, s$digitos$s) AGRUPACION,
              D_MOVIMIENTO.FECHA,
              TIPOMOVIMIENTO.CONCEPTO
         FROM D_MOVIMIENTO
         LEFT JOIN TIPOMOVIMIENTO
           ON D_MOVIMIENTO.COMPANIA        = TIPOMOVIMIENTO.COMPANIA
          AND D_MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
        WHERE D_MOVIMIENTO.COMPANIA     = 's$compania$s'
          AND TIPOMOVIMIENTO.CLASE        = 's$clase$s'
          AND TIPOMOVIMIENTO.TIPOELEMENTO = 's$tipo$s'
          AND D_MOVIMIENTO.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
          AND D_MOVIMIENTO.IND_REG NOT IN (0)
        GROUP BY D_MOVIMIENTO.COMPANIA,
              D_MOVIMIENTO.TIPOMOVIMIENTO,
              TIPOMOVIMIENTO.CLASE,
              TIPOMOVIMIENTO.TIPOELEMENTO,
              TIPOMOVIMIENTO.NOMBRECORTO,
              SUBSTR(D_MOVIMIENTO.ELEMENTO, 0, s$digitos$s),
              D_MOVIMIENTO.FECHA,
              TIPOMOVIMIENTO.CONCEPTO,
              D_MOVIMIENTO.IND_REG ) CINGRESOSCONSUMO
        GROUP BY CINGRESOSCONSUMO.COMPANIA,
              CINGRESOSCONSUMO.AGRUPACION)
SELECT INVENTARIO.COMPANIA,
       INVENTARIO.NOMBRELARGO,
       LENGTH(INVENTARIO.CODIGOELEMENTO) EXPR1,
       INVENTARIO.CODIGOELEMENTO AGRUPACION,
       SUM(NVL(C_RESUMENINGEGR.TOTAL,0)) TOTAL
  FROM INVENTARIO
 INNER JOIN C_RESUMENINGEGR
    ON INVENTARIO.COMPANIA       = C_RESUMENINGEGR.COMPANIA
   AND INVENTARIO.CODIGOELEMENTO = C_RESUMENINGEGR.AGRUPACION
 WHERE C_RESUMENINGEGR.TOTAL > 0
 GROUP BY INVENTARIO.COMPANIA,
       INVENTARIO.NOMBRELARGO,
       LENGTH(INVENTARIO.CODIGOELEMENTO),
       INVENTARIO.CODIGOELEMENTO,
       INVENTARIO.TIPO
