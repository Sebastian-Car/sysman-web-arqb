SELECT * FROM (
    SELECT  ORDENDECOMPRA.COMPANIA,
            TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY') ANIO ,
            TIPOORDENDECOMPRA.NOMBRE
    FROM ORDENDECOMPRA 
        INNER JOIN TIPOORDENDECOMPRA 
            ON  ORDENDECOMPRA.COMPANIA    = TIPOORDENDECOMPRA.COMPANIA
            AND ORDENDECOMPRA.CLASEORDEN  = TIPOORDENDECOMPRA.CODIGO 
WHERE ORDENDECOMPRA.COMPANIA            = 's$compania$s' 
  AND ORDENDECOMPRA.DEPENDENCIA         = 's$dependencia$s'
  AND TO_NUMBER(TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY')) 
  		BETWEEN s$anioInicial$s  AND  s$anioFinal$s    
) P PIVOT (COUNT(COMPANIA) FOR NOMBRE IN( s$pivot$s ))
