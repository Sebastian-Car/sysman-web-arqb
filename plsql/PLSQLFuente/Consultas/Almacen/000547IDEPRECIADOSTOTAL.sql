SELECT DISTINCT
  DEPRECIAR.ELEMENTO,
  DEPRECIAR.SERIE,
  INVENTARIO.NOMBRELARGO,
  DEPRECIAR.VLRELEMENTO,
  DEPRECIAR.DEPACUMULADAAJ,
  DEPRECIAR.COSTOAJUSTADO,
  DEPRECIAR.VLRDEPRECIACIONAJ,
  DEPRECIAR.VLRLIBROS
FROM DEPRECIAR 
  LEFT JOIN DEVOLUTIVO 
    ON  DEPRECIAR.COMPANIA = DEVOLUTIVO.COMPANIA
    AND DEPRECIAR.ELEMENTO = DEVOLUTIVO.ELEMENTO
    AND DEPRECIAR.SERIE = DEVOLUTIVO.SERIE
  LEFT JOIN INVENTARIO 
    ON  DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA
    AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
WHERE DEPRECIAR.COMPANIA=s$compania$s
  AND DEPRECIAR.VLRELEMENTO>0
  AND DEPRECIAR.VLRDEPRECIACIONAJ=0
  AND DEPRECIAR.VLRLIBROS=0 
  AND DEVOLUTIVO.TIPOELEMENTO='N'
