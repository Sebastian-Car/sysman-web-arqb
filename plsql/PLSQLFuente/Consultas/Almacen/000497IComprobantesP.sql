MERGE INTO CONSULTAS FIN USING (SELECT '000497IComprobantesP' INFORME ,TO_CLOB(q'[SELECT
    FECHA,
    CLASE,
    NUMERO,
    VALORTOTAL,
    TIPOMOVIMIENTO,
    REGISTRADO1,
    ENTRADAS,
    SALIDAS,
    DESCARGOS,
    TRASPASOS,
    OTROS_MOV,
    NOMBRE,
    NOMBRECORTO,
    NIT,
    NOMBRETERCERO
FROM
    (
    SELECT
        TO_CHAR(MOVIMIENTO.FECHA, 'DD/MM/YYYY') FECHA,
        CASE
            WHEN MOVIMIENTO.CLASE IN 'E'
    THEN 'Entradas'
            ELSE
            CASE
                WHEN MOVIMIENTO.CLASE IN 'S'
        THEN 'Salidas'
                ELSE
                CASE
                    WHEN MOVIMIENTO.CLASE IN 'T'
                    AND MOVIMIENTO.TIPOMOVIMIENTO NOT IN 'SDF'
            THEN 'Otros Movimientos'
                    ELSE
                    CASE
                        WHEN MOVIMIENTO.CLASE IN 'D'
                THEN 'Descargos'
                        ELSE 'Traspasos'
                    END
                END
            END
        END CLASE,
        MOVIMIENTO.NUMERO,
        MOVIMIENTO.VALORTOTALCONIVA VALORTOTAL,
        MOVIMIENTO.TIPOMOVIMIENTO,
        CASE
            WHEN REGISTRADO NOT IN 0
    THEN 'SI'
            ELSE 'NO'
        END REGISTRADO1,
        CASE
            WHEN MOVIMIENTO.CLASE = 'E'
    THEN MOVIMIENTO.VALORTOTALCONIVA
            ELSE 0
        END ENTRADAS,
        CASE
            WHEN MOVIMIENTO.CLASE = 'S'
    THEN MOVIMIENTO.VALORTOTALCONIVA
            ELSE 0
        END SALIDAS,
        CASE
            WHEN MOVIMIENTO.CLASE = 'D'
    THEN MOVIMIENTO.VALORTOTALCONIVA
            ELSE 0
        END DESCARGOS,
        CASE
            WHEN MOVIMIENTO.CLASE = 'T'
            AND MOVIMIENTO.TIPOMOVIMIENTO NOT IN 'SDF'
    THEN MOVIMIENTO.VALORTOTALCONIVA
            ELSE 0
        END TRASPASOS,
        CASE
            WHEN MOVIMIENTO.TIPOMOVIMIENTO IN 'SDF'
    THEN SUM(D_MOVIMIENTO.VALORTOTAL)
            ELSE 0
        END OTROS_MOV,
        TIPOMOVIMIENTO.NOMBRE,
        TIPOMOVIMIENTO.NOMBRECORTO,
        TERCERO.NIT,
        TERCERO.NOMBRE NOMBRETERCERO
    FROM
        V_MOVIMIENTOS_TOTALES MOVIMIENTO
    INNER JOIN TIPOMOVIMIENTO
ON
        MOVIMIENTO.COMPANIA = TIPOMOVIMIENTO.COMPANIA
        AND MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
    INNER JOIN TERCERO
ON
        MOVIMIENTO.COMPANIA = TERCERO.COMPANIA
        AND MOVIMIENTO.TERCERO = TERCERO.NIT
        AND MOVIMIENTO.SUCURSAL = TERCERO.SUCURSAL
    LEFT JOIN D_MOVIMIENTO
ON
        MOVIMIENTO.COMPANIA = D_MOVIMIENTO.COMPANIA
        AND MOVIMIENTO.TIPOMOVIMIENTO = D_MOVIMIENTO.TIPOMOVIMIENTO
        AND MOVIMIENTO.NUMERO = D_MOVIMIENTO.MOVIMIENTO
    WHERE
        MOVIMIENTO.COMPANIA = s$compania$s
        AND TO_CHAR(MOVIMIENTO.FECHA, 'dd/MM/yyyy')= 's$desde$s'
    GROUP BY
        MOVIMIENTO.FECHA,
        MOVIMIENTO.REGISTRADO,
        MOVIMIENTO.CLASE,
        MOVIMIENTO.NUMERO,
        MOVIMIENTO.VALORTOTALCONIVA,
        MOVIMIENTO.TIPOMOVIMIENTO,
        TIPOMOVIMIENTO.NOMBRE,
        TIPOMOVIMIENTO.NOMBRECORTO,
        TERCERO.NIT,
        TERCERO.NOMBRE )]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);