SELECT     PREDIOS.ID_PREDIO, 
           PREDIOS.NOMBRE PNOMBRE, 
           PREDIOS.DIRECCION, 
           BI_UBICACION.NOMBRE UNOMBRE, 
           PREDIOS.DESTINACION, 
           PREDIOS.USO_ACTUAL, 
           PREDIOS.ESCRITURA_NO, 
           PREDIOS.FECHA_ESCRITURA, 
           PREDIOS.NOTARIA, 
           CIUDAD.NOMBRE CNOMBRE, 
           DEPARTAMENTO.NOMBRE DNOMBRE, 
           MODALIDAD.DESCRIPCION MODALIDAD, 
           PREDIOS.AREA_ESCRITURA, 
           PREDIOS.MATR_INMOVILIARIA, 
           PREDIOS.COD_PREDIAL, 
           SECTOR_INMUEBLE.DESCRIPCION TIPO_PREDIO, 
           PREDIOS.ACC_PRINCIPAL, 
           PREDIOS.ACC_SECUNDARIA, 
           PREDIOS.ACC_ASFALTO, 
           PREDIOS.ACC_RIGIDO, 
           PREDIOS.ACC_ADOQUIN, 
           PREDIOS.PREDIO_REGULAR, 
           PREDIOS.PREDIO_IRREGULAR, 
           PREDIOS.PREDIO_INCLINADO, 
           PREDIOS.PREDIO_PLANO, 
           PREDIOS.NECESITA_INVENTARIO, 
           PREDIOS.ANO_CONSTRUCCION, 
           PREDIOS.EXT_LINDEROS_FRENTE, 
           PREDIOS.EXT_LINDEROS_IZQ, 
           PREDIOS.EXT_LINDEROS_FONDO, 
           PREDIOS.EXT_LINDEROS_DER, 
           PREDIOS.DESC_LINDEROS_FRENTE, 
           PREDIOS.DESC_LINDEROS_IZQ, 
           PREDIOS.DESC_LINDEROS_FONDO, 
           PREDIOS.DESC_LINDEROS_DER, 
           HIST_AVALUOS.AREA_LOTE, 
           HIST_AVALUOS.AREA_CONSTRUCCION, 
           HIST_AVALUOS.LONG_CERRAMIENTO, 
           HIST_AVALUOS.AREA_ZONA_DURA, 
           HIST_AVALUOS.VAL_UNIDAD_LOTE, 
           HIST_AVALUOS.VAL_UNIDAD_CONSTRUCCION, 
           HIST_AVALUOS.VAL_UNIDAD_CERRAMIENTO, 
           HIST_AVALUOS.VAL_UNIDAD_ZONA_DURA, 
           HIST_AVALUOS.VAL_TOTAL_LOTE, 
           HIST_AVALUOS.VAL_TOTAL_CONSTRUCCION, 
           HIST_AVALUOS.VAL_TOTAL_CERRAMIENTO, 
           HIST_AVALUOS.VAL_TOTAL_ZONADURA, 
           HIST_AVALUOS.VIDA_UTIL, 
           PREDIOS.RUTA_FOTO, 
           PREDIOS.COD_FOTO, 
           PREDIOS.OBSERVACIONES,  
           PREDIOS.ENCUESTA_VALOR, 
           PREDIOS.SERIE_PLACA
FROM       PREDIOS 
LEFT JOIN  BI_UBICACION ON (PREDIOS.COMPANIA = BI_UBICACION.COMPANIA  
     AND   PREDIOS.UBICACION = BI_UBICACION.CODIGO) 
LEFT JOIN  DEPARTAMENTO ON (PREDIOS.PAIS = DEPARTAMENTO.PAIS  
     AND   PREDIOS.DPTO_ESCRITURA = DEPARTAMENTO.CODIGO ) 
LEFT JOIN  CIUDAD ON (PREDIOS.PAIS = CIUDAD.PAIS  
     AND   PREDIOS.DPTO_ESCRITURA = CIUDAD.DEPARTAMENTO 
     AND   PREDIOS.CIUDAD_ESCRITURA = CIUDAD.CODIGO) 
LEFT JOIN  HIST_AVALUOS ON (PREDIOS.COMPANIA = HIST_AVALUOS.COMPANIA
     AND   PREDIOS.ID_PREDIO = HIST_AVALUOS.ID_INMUEBLE)
INNER JOIN MODALIDAD ON(PREDIOS.COMPANIA = MODALIDAD.COMPANIA
     AND   PREDIOS.MODALIDAD = MODALIDAD.CODIGO_MODALIDAD)
INNER JOIN SECTOR_INMUEBLE ON (PREDIOS.COMPANIA = SECTOR_INMUEBLE.COMPANIA
     AND   PREDIOS.TIPO_PREDIO = SECTOR_INMUEBLE.CODIGO_SECTOR)
 WHERE PREDIOS.COMPANIA =  s$compania$s

