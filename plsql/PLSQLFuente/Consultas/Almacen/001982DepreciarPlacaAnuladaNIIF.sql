SELECT 
       DEPRECIAR.ELEMENTO,
       DEPRECIAR.SERIE,
       INVENTARIO.NOMBRELARGO || ' ' || DEVOLUTIVO.DESCRIPCION || 
       CASE WHEN DEVOLUTIVO.MARCA IS NULL OR DEVOLUTIVO.MARCA = ' 'THEN '' ELSE ' MARCA: ' || DEVOLUTIVO.MARCA END AS NOMBRE_LARGO
      ,DEVOLUTIVO.NIIF_VALOR_BASE
      ,DEPRECIAR.NIIF_VLRDEPRECIACION
      ,DEPRECIAR.NIIF_DEPACUMULADA
      ,(DEPRECIAR.NIIF_DEPACUMULADA - DEPRECIAR.NIIF_VLRDEPRECIACION) AS DEPRECIACION_ANTERIOR
      ,CASE WHEN DEPRECIAR.NIIF_VLRLIBROS < 0 
            THEN DEVOLUTIVO.NIIF_VALOR_BASE - DEPRECIAR.NIIF_DEPACUMULADA
            ELSE DEPRECIAR.NIIF_VLRLIBROS 
        END AS VLRLIBROS
      ,NIIF_INVENTARIOCONTA.CUENTACREDITO AS CODIGOCUENTA
      ,PLAN_CONTABLE.NOMBRE AS NOMBRECUENTA
      ,LPAD(DEPRECIAR.ELEMENTO, s$agrupacion$s) AS AGRUPACION
      ,PCK_ALMACEN.FC_NOMBREINVENTARIO(DEVOLUTIVO.COMPANIA, SUBSTR(DEPRECIAR.ELEMENTO,1,s$agrupacion$s), 0) AS NOMBRE_INVENTARIO
  FROM DEPRECIAR
 INNER JOIN DEVOLUTIVO
    ON DEPRECIAR.COMPANIA = DEVOLUTIVO.COMPANIA
   AND DEPRECIAR.ELEMENTO = DEVOLUTIVO.ELEMENTO
   AND DEPRECIAR.SERIE    = DEVOLUTIVO.SERIE
 INNER JOIN INVENTARIO
    ON DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA
   AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
  LEFT JOIN NIIF_INVENTARIOCONTA 
    ON DEPRECIAR.COMPANIA                           = NIIF_INVENTARIOCONTA.COMPANIA
   AND SUBSTR(DEPRECIAR.ELEMENTO,0, s$agrupacion$s) = NIIF_INVENTARIOCONTA.CODIGOELEMENTO 
   AND DEPRECIAR.ANO                                = NIIF_INVENTARIOCONTA.ANO 
   AND DEPRECIAR.NIIF_TIPO_ACTIVO                   = NIIF_INVENTARIOCONTA.TIPOACTIVO
   AND DEPRECIAR.BODEGA                             = NIIF_INVENTARIOCONTA.BODEGA
   AND DEPRECIAR.CENTRO_COSTO                       = NIIF_INVENTARIOCONTA.CENTRO_COSTO
  LEFT JOIN PLAN_CONTABLE
    ON NIIF_INVENTARIOCONTA.COMPANIA      = PLAN_CONTABLE.COMPANIA
   AND NIIF_INVENTARIOCONTA.ANO           = PLAN_CONTABLE.ANO
   AND NIIF_INVENTARIOCONTA.CUENTACREDITO = PLAN_CONTABLE.CODIGO
WHERE DEPRECIAR.COMPANIA       = s$compania$s 
  AND TRUNC(DEPRECIAR.PERIODO) = TO_DATE('s$ultimoDia$s','DD/MM/YYYY') 
  AND DEPRECIAR.ELEMENTO BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s'
  AND DEVOLUTIVO.PLACAANULADA NOT IN (0)
  AND DEVOLUTIVO.FECHAANULADA BETWEEN TO_DATE('s$primerDia$s', 'DD/MM/YYYY')  AND TO_DATE('s$ultimoDia$s', 'DD/MM/YYYY')  
  AND TRUNC(DEPRECIAR.NIIF_FECHAFUNCIONAMIENTO) <= TO_DATE('s$ultimoDia$s', 'DD/MM/YYYY')  
ORDER BY DEPRECIAR.ELEMENTO
       , DEPRECIAR.SERIE
       , DEPRECIAR.PERIODO  
