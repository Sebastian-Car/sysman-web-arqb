SELECT 
DEPRECIAR.ELEMENTO, 
DEPRECIAR.SERIE, 
INVENTARIO.NOMBRELARGO ||' '|| DEVOLUTIVO.DESCRIPCION || CASE WHEN TRIM(DEVOLUTIVO.MARCA) IS NULL THEN '' ELSE ' MARCA: ' ||DEVOLUTIVO.MARCA END NOMBRELARGOS, 
PCK_ALMACEN.FC_NOMBREINVENTARIO(DEPRECIAR.COMPANIA,SUBSTR(DEPRECIAR.ELEMENTO,1, s$agrupacion$s),0) NOMBREINVENTARIO,
DEPRECIAR.VLRELEMENTO, 
DEPRECIAR.VLRDEPRECIACION, 
DEPRECIAR.COSTOAJUSTADO, 
DEPRECIAR.VLRLIBROS, 
DEPRECIAR.DEPACUMULADA, 
SUBSTR(DEPRECIAR.ELEMENTO, 1, s$agrupacion$s) AGRUPACION, 
' ' CodigoCuenta, 
' ' NombreCuenta
FROM DEPRECIAR 
  INNER JOIN DEVOLUTIVO ON DEPRECIAR.COMPANIA = DEVOLUTIVO.COMPANIA
    AND DEPRECIAR.ELEMENTO = DEVOLUTIVO.ELEMENTO 
    AND DEPRECIAR.SERIE = DEVOLUTIVO.SERIE
  INNER JOIN INVENTARIO ON DEPRECIAR.COMPANIA = INVENTARIO.COMPANIA
    AND DEPRECIAR.ELEMENTO = INVENTARIO.CODIGOELEMENTO
  INNER JOIN DEPENDENCIA
    ON DEPRECIAR.COMPANIA = DEPENDENCIA.COMPANIA
    AND DEPRECIAR.DEPENDENCIA = DEPENDENCIA.CODIGO
  INNER JOIN BODEGA
    ON DEPRECIAR.COMPANIA = BODEGA.COMPANIA
    AND DEPRECIAR.BODEGA = BODEGA.CODIGO
WHERE DEPRECIAR.COMPANIA=s$compania$s
  AND DEPRECIAR.ELEMENTO BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s' 
  AND TO_CHAR(DEPRECIAR.PERIODO,'DD/MM/YYYY')='s$ultimoDia$s'
  AND TO_DATE(TO_CHAR(NVL(FECHAANULADA,TO_DATE('31/12/3000')), 'DD/MM/YYYY'), 'DD/MM/YYYY')>TO_DATE('s$ultimoDia$s', 'DD/MM/YYYY')
  AND TO_DATE(TO_CHAR(DEVOLUTIVO.FECHAADQUISICION, 'DD/MM/YYYY'), 'DD/MM/YYYY')<=TO_DATE('s$ultimoDia$s', 'DD/MM/YYYY')
  AND BODEGA.CLASE_BODEGA NOT IN (20,40,50)
  AND DEPENDENCIA.COMODATO IN (0)
	s$filtro$s
ORDER BY DEPRECIAR.COMPANIA, DEPRECIAR.ELEMENTO, DEPRECIAR.SERIE, DEPRECIAR.PERIODO
