SELECT D_MOVIMIENTO.TIPOMOVIMIENTO,
  SUM(D_MOVIMIENTO.VALORTOTAL) SUMADEVALORTOTAL,
  SUM(D_MOVIMIENTO.CANTIDAD) SUMADECANTIDAD,
  D_MOVIMIENTO.ELEMENTO,
  D_MOVIMIENTO.FECHA,
  MOVIMIENTO.DEPENDENCIA_DESTINO DEPENDENCIA,
  TIPOMOVIMIENTO.CONCEPTO,
  DEPENDENCIA.NOMBRE  NOMDEPEDENCIA,
  D_MOVIMIENTO.COMPANIA,
  INVENTARIO.NOMBRELARGO,
  D_MOVIMIENTO.IND_REG,
  D_MOVIMIENTO.MOVIMIENTO
FROM D_MOVIMIENTO
LEFT JOIN TIPOMOVIMIENTO
ON D_MOVIMIENTO.COMPANIA        = TIPOMOVIMIENTO.COMPANIA
AND D_MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
INNER JOIN MOVIMIENTO
ON D_MOVIMIENTO.COMPANIA        = MOVIMIENTO.COMPANIA
AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO
AND D_MOVIMIENTO.MOVIMIENTO     = MOVIMIENTO.NUMERO
INNER JOIN DEPENDENCIA
ON MOVIMIENTO.COMPANIA             = DEPENDENCIA.COMPANIA
AND MOVIMIENTO.DEPENDENCIA_DESTINO = DEPENDENCIA.CODIGO
INNER JOIN INVENTARIO
ON D_MOVIMIENTO.COMPANIA    = INVENTARIO.COMPANIA
AND D_MOVIMIENTO.ELEMENTO   = INVENTARIO.CODIGOELEMENTO
WHERE D_MOVIMIENTO.COMPANIA = s$compania$s
AND D_MOVIMIENTO.ELEMENTO BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s' 
AND D_MOVIMIENTO.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
AND MOVIMIENTO.DEPENDENCIA_DESTINO BETWEEN 's$dependenciaInicial$s' AND 's$dependenciaFinal$s'
AND TIPOMOVIMIENTO.CLASE        = 'S'
AND TIPOMOVIMIENTO.TIPOELEMENTO = 'C'
AND D_MOVIMIENTO.IND_REG       <> 0
AND TIPOMOVIMIENTO.CONCEPTO     = 'N'
GROUP BY D_MOVIMIENTO.TIPOMOVIMIENTO,
  D_MOVIMIENTO.ELEMENTO,
  D_MOVIMIENTO.FECHA,
  MOVIMIENTO.DEPENDENCIA_DESTINO,
  TIPOMOVIMIENTO.CONCEPTO,
  DEPENDENCIA.NOMBRE,
  D_MOVIMIENTO.COMPANIA,
  INVENTARIO.NOMBRELARGO,
  D_MOVIMIENTO.IND_REG,
  D_MOVIMIENTO.MOVIMIENTO
