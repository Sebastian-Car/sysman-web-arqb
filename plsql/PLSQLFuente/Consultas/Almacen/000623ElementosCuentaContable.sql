WITH VISTA_ALMACEN AS (SELECT  
        V_BASE_CUENTAALMACEN.COMPANIA ,  
        V_BASE_CUENTAALMACEN.CUENTA, 
        CASE WHEN  V_BASE_CUENTAALMACEN.CUENTA IS NULL THEN '' ELSE  PLAN_CONTABLE.NOMBRE END NOMBRECUENTA, 
        SUM(NVL(VALOR_ENTRADAS,0)- NVL(VALOR_SALIDAS,0)+ NVL(AJUSTESDEBITO,0) + NVL(AJUSTESCREDITO,0)) AS SALDOFINAL, 
        V_BASE_CUENTAALMACEN.NOMBREBODEGA DEPEND, 
        V_BASE_CUENTAALMACEN.TIPO, 
        V_BASE_CUENTAALMACEN.SERIE 
FROM  V_BASE_CUENTAALMACEN 
 LEFT JOIN (SELECT COMPANIA, ANO, CODIGO, NOMBRE
             FROM PLAN_CONTABLE
             WHERE COMPANIA = s$compania$s
               AND ANO      = TO_NUMBER(TO_CHAR(s$fechaFinal$s, 'YYYY'))) PLAN_CONTABLE 
    ON PLAN_CONTABLE.COMPANIA  = V_BASE_CUENTAALMACEN.COMPANIA
  AND PLAN_CONTABLE.CODIGO    = V_BASE_CUENTAALMACEN.CUENTA
  AND PLAN_CONTABLE.ANO       = TO_NUMBER(TO_CHAR(s$fechaFinal$s, 'YYYY'))              
WHERE V_BASE_CUENTAALMACEN.COMPANIA = s$compania$s 
  AND V_BASE_CUENTAALMACEN.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s  
  AND V_BASE_CUENTAALMACEN.TIPO IN ('D' , 'N') 
GROUP BY   
           V_BASE_CUENTAALMACEN.COMPANIA , 
           V_BASE_CUENTAALMACEN.CUENTA, 
           CASE WHEN  V_BASE_CUENTAALMACEN.CUENTA IS NULL THEN '' ELSE  PLAN_CONTABLE.NOMBRE END, 
           V_BASE_CUENTAALMACEN.NOMBREBODEGA, 
           V_BASE_CUENTAALMACEN.TIPO, 
           V_BASE_CUENTAALMACEN.SERIE) 
SELECT VISTA_ALMACEN.CUENTA, 
       VISTA_ALMACEN.NOMBRECUENTA, 
       VISTA_ALMACEN.SALDOFINAL, 
       VISTA_ALMACEN.DEPEND, 
       VISTA_ALMACEN.SERIE, 
       INVENTARIO.CODIGOELEMENTO, 
       INVENTARIO.NOMBRELARGO, 
       DEVOLUTIVO.DESCRIPCION 
FROM DEVOLUTIVO   
   INNER JOIN VISTA_ALMACEN  
     ON VISTA_ALMACEN.COMPANIA = DEVOLUTIVO.COMPANIA  
    AND VISTA_ALMACEN.SERIE    = DEVOLUTIVO.SERIE  
   INNER JOIN INVENTARIO  
     ON DEVOLUTIVO.COMPANIA    = INVENTARIO.COMPANIA  
    AND DEVOLUTIVO.ELEMENTO    = INVENTARIO.CODIGOELEMENTO 
WHERE VISTA_ALMACEN.SALDOFINAL >0
