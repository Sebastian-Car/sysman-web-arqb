SELECT    VIAS.ID_VIA, 
          VIAS.NOMBRE, 
          TIPO_VIA.DESCRIPCION CLASE_VIA, 
          ESTADO_VIA.DESCRIPCION ESTADO, 
          VIAS.ANCHO_VIA, 
          VIAS.SECTOR, 
          VIAS.NO_TRAMO, 
          VIAS.INICIO_TRAMO, 
          VIAS.FIN_TRAMO, 
          ITEMS.CODIGO_ITEM, 
          ITEMS.DESCRIPCION, 
          VIAS_ITEMS.ACABADO, 
          VIAS_ITEMS.ESTADO  IESTADO, 
          VIAS_ITEMS.LONGITUD, 
          VIAS_ITEMS.ANCHO, 
          VIAS.OBSERVACIONES, 
          HIST_AVALUO_VIA.AREA_TERRENO, 
          HIST_AVALUO_VIA.VAL_UNITARIO, 
          HIST_AVALUO_VIA.VAL_TERRENO, 
          HIST_AVALUO_VIA.VAL_OBRA, 
          HIST_AVALUO_VIA.VIDA_UTIL, 
          VIAS.RUTA_FOTO,
          VIAS.COD_FOTO,  
          VIAS.LONG_SARNEL_SEPDOR, 
          VIAS.SERIE_PLACA
FROM      VIAS 
LEFT JOIN HIST_AVALUO_VIA ON (VIAS.COMPANIA = HIST_AVALUO_VIA.COMPANIA
     AND VIAS.ID_VIA = HIST_AVALUO_VIA.ID_VIA) 
LEFT JOIN COMPANIA ON (VIAS.COMPANIA = COMPANIA.CODIGO) 
LEFT JOIN VIAS_ITEMS ON (VIAS.ID_VIA = VIAS_ITEMS.ID_VIA 
     AND VIAS.COMPANIA = VIAS_ITEMS.COMPANIA) 
LEFT JOIN ITEMS ON (VIAS_ITEMS.CODIGO_ITEM = ITEMS.CODIGO_ITEM 
     AND VIAS_ITEMS.COMPANIA = ITEMS.COMPANIA)
INNER JOIN ESTADO_VIA ON(VIAS.COMPANIA = ESTADO_VIA.COMPANIA
     AND VIAS.ESTADO = ESTADO_VIA.CODIGO_ESTADO)
INNER JOIN TIPO_VIA ON (VIAS.COMPANIA = TIPO_VIA.COMPANIA
     AND VIAS.CLASE_VIA = TIPO_VIA.CODIGO_TIPO)
 WHERE VIAS.COMPANIA =  s$compania$s
 
