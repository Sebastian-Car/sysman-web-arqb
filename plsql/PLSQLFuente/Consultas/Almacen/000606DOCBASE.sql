SELECT  FECHA FECHA_SOLICITUD, TE.NOMBRE TIPO_EVENTO, DESCRIPCION, RESPUESTA
FROM    TRANSACCION_DEVOLUTIVO TD
        INNER JOIN TIPO_EVENTO TE
        ON  TD.COMPANIA    = TE.COMPANIA
        AND TD.TIPO_EVENTO = TE.CODIGO
WHERE   TD.COMPANIA = s$compania$s
AND     TD.TIPO = 's$tipo$s'
AND     TD.CODIGO = s$codigo$s
