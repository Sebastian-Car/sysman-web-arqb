SELECT DISTINCT  
  INVENTARIO.COMPANIA, 
  s$orden2$s ORDEN2, 
  s$orden3$s ORDEN3, 
  INVENTARIO.CODIGOELEMENTO, 
  INVENTARIO.NOMBRELARGO, 
  INVENTARIO.EXISTENCIA, 
  INVENTARIO.VLRUNITARIOPROM VALORUNITARIO, 
  INVENTARIO.TIPO, 
  INVENTARIO.REFERENCIA, 
  INVENTARIO.UNIDAD, 
  INVENTARIO.MEDIDA, 
  INVENTARIO.MARCA, 
  INVENTARIO.LOCALIZACION, 
  PCK_ALMACEN.FC_NOMBREINVENTARIO(INVENTARIO.COMPANIA, SUBSTR(INVENTARIO.CODIGOELEMENTO,0,s$grupo$s), 0) NOMBREINVENTARIO, 
  SUBSTR(INVENTARIO.CODIGOELEMENTO,0,s$grupo$s) SIG, 
  INVENTARIO.CANTRESERVADA, 
  INVENTARIO.CANTCOMPORLLEGAR, 
  INVENTARIO.CANTINSERVIBLE, 
  SUM(NVL(ENTRADAS,0)-NVL(SALIDAS,0)+NVL(CANTIDADAJUSTESDEBITO,0)+NVL(CANTIDADAJUSTESCREDITO,0)) TCANTIDAD, 
  VLRUNITARIOPROM*EXISTENCIA VALORTOTAL,  
  V_BASE_CUENTAALMACEN.DEPENDENCIA_DESTINO DEPENDENCIA, 
  TO_DATE('s$fechaFinal$s','DD/MM/YYYY') FECHAFINAL 
FROM INVENTARIO  
  LEFT JOIN V_BASE_CUENTAALMACEN 
    ON  INVENTARIO.COMPANIA = V_BASE_CUENTAALMACEN.COMPANIA 
    AND INVENTARIO.CODIGOELEMENTO = V_BASE_CUENTAALMACEN.ELEMENTO 
WHERE INVENTARIO.COMPANIA='s$companiaSel$s' 
  AND INVENTARIO.CODIGOELEMENTO BETWEEN 's$elementoDesde$s' AND 's$elementoHasta$s' 
  AND V_BASE_CUENTAALMACEN.FECHA BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY') 
  s$conSaldoCero$s  
  AND V_BASE_CUENTAALMACEN.DEPENDENCIA_DESTINO LIKE '00000%'  
GROUP BY 
  INVENTARIO.COMPANIA, INVENTARIO.CODIGOELEMENTO, INVENTARIO.NOMBRELARGO, INVENTARIO.EXISTENCIA, INVENTARIO.VLRUNITARIOPROM,  
  INVENTARIO.TIPO, INVENTARIO.REFERENCIA, INVENTARIO.UNIDAD, INVENTARIO.MEDIDA, INVENTARIO.MARCA,  
  INVENTARIO.LOCALIZACION, SUBSTR(INVENTARIO.CODIGOELEMENTO,0,5), INVENTARIO.CANTRESERVADA, INVENTARIO.CANTCOMPORLLEGAR, INVENTARIO.CANTINSERVIBLE,  
VLRUNITARIOPROM*EXISTENCIA, V_BASE_CUENTAALMACEN.DEPENDENCIA_DESTINO, TO_DATE('s$fechaFinal$s','DD/MM/YYYY') 
ORDER BY INVENTARIO.CODIGOELEMENTO
