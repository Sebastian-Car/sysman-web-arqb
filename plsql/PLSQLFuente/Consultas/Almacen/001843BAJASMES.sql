SELECT DISTINCT
        DEVOLUTIVO.ELEMENTO, 
        DEVOLUTIVO.SERIE,  
        INVENTARIO.NOMBRECORTO, 
        DEVOLUTIVO.VALOR AS VALORHISTO, 
        DEVOLUTIVO.FECHAADQUISICION, 
        DEVOLUTIVO.FECHASALIDASERVICIO, 
        DEVOLUTIVO.DEPACUMULADA, 
        ACTDEPRECIABLE.MESESVIDAUTIL, 
        V_DESCARGOBODEGA.TIPOMOVIMIENTO AS CLASEDESCARGO, 
        V_DESCARGOBODEGA.NUMERO AS DESCARGO,
        V_DESCARGOBODEGA.DESCRIPCION,
        V_DESCARGOBODEGA.FECHA AS FECHABAJA,
        DEVOLUTIVO.VLRLIBROS 
        FROM DEVOLUTIVO 
        INNER JOIN INVENTARIO 
          ON DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA 
          AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
        INNER JOIN ACTDEPRECIABLE
          ON INVENTARIO.TIPOACTIVO = ACTDEPRECIABLE.CODIGO
        LEFT JOIN D_MOVIMIENTO
          ON D_MOVIMIENTO.COMPANIA = INVENTARIO.COMPANIA
          AND D_MOVIMIENTO.ELEMENTO = INVENTARIO.CODIGOELEMENTO       
        INNER JOIN V_DESCARGOBODEGA
          ON D_MOVIMIENTO.COMPANIA = V_DESCARGOBODEGA. COMPANIA
          AND D_MOVIMIENTO.TIPOMOVIMIENTO = V_DESCARGOBODEGA.TIPOMOVIMIENTO
          AND D_MOVIMIENTO.MOVIMIENTO = V_DESCARGOBODEGA.NUMERO
        WHERE DEVOLUTIVO.COMPANIA = 's$compania$s'
          AND DEVOLUTIVO.PLACAANULADA NOT IN (0)
          AND DEVOLUTIVO.FECHAANULADA BETWEEN TO_DATE('01/' || 's$mesInicial$s' || '/' || 's$ano$s', 'DD/MM/YYYY')
                                              AND TO_DATE('28/' || 's$mesFinal$s' || '/' || 's$ano$s', 'DD/MM/YYYY')
