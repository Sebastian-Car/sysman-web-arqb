SELECT INVENTARIO.COMPANIA, 
                s$ORDEN2$s AS ORDEN2, 
                s$ORDEN3$s AS ORDEN3, 
                PCK_ALMACEN.FC_NOMBREINVENTARIO(INVENTARIO.COMPANIA,SUBSTR(CODIGOELEMENTO,0,s$PR_DIGITOSAGRUPACIONINV$s),0) AS NOMBREINVENTARIO,
                INVENTARIO.CODIGOELEMENTO, 
                INVENTARIO.NOMBRELARGO, 
                INVENTARIO.TIPO, 
                INVENTARIO.REFERENCIA, 
                INVENTARIO.UNIDAD, 
                INVENTARIO.MEDIDA, 
                INVENTARIO.MARCA,
                INVENTARIO.LOCALIZACION,
                SUBSTR(CODIGOELEMENTO,0,s$PR_DIGITOSAGRUPACIONINV$s) AS SIG,
                INVENTARIO.CANTRESERVADA,
                INVENTARIO.CANTCOMPORLLEGAR,
                INVENTARIO.CANTINSERVIBLE, 
                LENGTH(CODIGOELEMENTO) AS EXPR1,
                ULTIMOMOVIMIENTOC_P.VPROMEDIO,
                NVL(ULTIMOMOVIMIENTOC_P.CSALDO,0) TCANTIDAD,
                NVL(ULTIMOMOVIMIENTOC_P.VSaldo,0) VSaldo,
                UNIDAD.NOMBRE, 
                INVENTARIO.VALORTOTAL AS VALORTOT,
                ULTIMOMOVIMIENTOC_P.VSALDO AS VSALDO2,
                0 AS EXPR2, 
                NVL(ULTIMOMOVIMIENTOC_P.VPROMEDIO,0) VALORPROM,
                INVENTARIO.VLRUNITARIOPROM,
                NVL(ULTIMOMOVIMIENTOC_P.CSALDO,0)*ULTIMOMOVIMIENTOC_P.VPROMEDIO AS VSALDO1,
                0 AS TCANTIDAD2 
                FROM INVENTARIO 
                LEFT JOIN         (SELECT 
                                  D_MOVIMIENTO.COMPANIA, 
                                  D_MOVIMIENTO.ELEMENTO, 
                                  MAX(D_MOVIMIENTO.TIPOMOVIMIENTO)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS ULTIMODETIPOMOVIMIENTO,
                                  MAX(D_MOVIMIENTO.MOVIMIENTO)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS ÚLTIMODEMOVIMIENTO,
                                  MAX(D_MOVIMIENTO.ESTADO)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS ÚLTIMODEESTADO,
                                  MAX(TO_CHAR(D_MOVIMIENTO.FECHA,'YYYYMMDD')) KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS FECHAX,
                                  MAX(D_MOVIMIENTO.FECHA)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS ÚLTIMODEFECHA,
                                  MAX(D_MOVIMIENTO.SALDOKARDEX)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS CSALDO,
                                  MAX(D_MOVIMIENTO.VALORSALDO)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS VSALDO, 
                                  MAX(D_MOVIMIENTO.VALORUNITARIO)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS VPROMEDIO
                                  FROM INVENTARIO 
                                  INNER JOIN (D_MOVIMIENTO 
                                      INNER JOIN V_MOVIMIENTO MOVIMIENTO ON 
                                      D_MOVIMIENTO.COMPANIA = MOVIMIENTO.COMPANIA AND 
                                      D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO AND 
                                      D_MOVIMIENTO.MOVIMIENTO = MOVIMIENTO.NUMERO) ON 
                                  INVENTARIO.CODIGOELEMENTO = D_MOVIMIENTO.ELEMENTO AND 
                                  INVENTARIO.EXISTENCIA = D_MOVIMIENTO.SALDOKARDEX AND 
                                  INVENTARIO.COMPANIA = D_MOVIMIENTO.COMPANIA
                                  WHERE D_MOVIMIENTO.COMPANIA=s$compania$s 
                                     AND D_MOVIMIENTO.ELEMENTO 
                                     BETWEEN s$ELEMENTODESDE$s AND s$ELEMENTOHASTA$s  
                                     AND D_MOVIMIENTO.FECHA<=s$FECHA$s 
                                    AND D_MOVIMIENTO.IND_REG NOT IN (0)
                                  GROUP BY D_MOVIMIENTO.COMPANIA, 
                                  D_MOVIMIENTO.ELEMENTO) ULTIMOMOVIMIENTOC_P ON 
                INVENTARIO.COMPANIA = ULTIMOMOVIMIENTOC_P.COMPANIA AND 
                INVENTARIO.CODIGOELEMENTO = ULTIMOMOVIMIENTOC_P.ELEMENTO 
                LEFT JOIN UNIDAD ON 
                INVENTARIO.UNIDAD = UNIDAD.UNIDAD
                WHERE INVENTARIO.COMPANIA=s$compania$s 
                  AND INVENTARIO.TIPO <> 'C'
                  AND INVENTARIO.CODIGOELEMENTO
                BETWEEN s$ELEMENTODESDE$s AND s$ELEMENTOHASTA$s 
                  AND LENGTH(CODIGOELEMENTO) >= s$PR_DIGITOSAGRUPACIONINV$s
                  AND INVENTARIO.TIENEMOVIMIENTO NOT IN (0)
                  s$SALDO1$s 
                UNION
                
                SELECT INVENTARIO.COMPANIA, 
                s$ORDEN2$s AS ORDEN2, 
                s$ORDEN3$s AS ORDEN3, 
                PCK_ALMACEN.FC_NOMBREINVENTARIO(INVENTARIO.COMPANIA,SUBSTR(CODIGOELEMENTO,0,s$PR_DIGITOSAGRUPACIONINV$s),0) AS NOMBREINVENTARIO,
                ULTIMOMOVIMIENTOC_PEPS_D.ELEMENTO AS CODIGOELEMENTO, 
                INVENTARIO.NOMBRELARGO, 
                ULTIMOMOVIMIENTOC_PEPS_D.TIPO, 
                INVENTARIO.REFERENCIA, 
                INVENTARIO.UNIDAD, 
                INVENTARIO.MEDIDA, 
                INVENTARIO.MARCA, 
                INVENTARIO.LOCALIZACION, 
                SUBSTR(ELEMENTO,0,s$PR_DIGITOSAGRUPACIONINV$s) AS SIG,
                INVENTARIO.CANTRESERVADA,
                INVENTARIO.CANTCOMPORLLEGAR, 
                INVENTARIO.CANTINSERVIBLE,
                LENGTH(ELEMENTO) AS EXPR1, 
                ULTIMOMOVIMIENTOC_PEPS_D.VALORUNITARIO AS VPROMEDIO,
                NVL(ULTIMOMOVIMIENTOC_PEPS_D.SALDO_PEPS,0) AS TCANTIDAD,
                NVL(ULTIMOMOVIMIENTOC_PEPS_D.VSALDO,0) AS VSALDO2,
                UNIDAD.NOMBRE,  
                ULTIMOMOVIMIENTOC_PEPS_D.VALORUNITARIO*ULTIMOMOVIMIENTOC_PEPS_D.SALDO_PEPS AS VALORTOT,
                ULTIMOMOVIMIENTOC_PEPS_D.VALORUNITARIO*ULTIMOMOVIMIENTOC_PEPS_D.SALDO_PEPS AS VSALDO,
                NVL(ULTIMOMOVIMIENTOC_PEPS_D.SALDO_PEPS,0) AS EXPR2,
                0 AS VALORPROM, 
                ULTIMOMOVIMIENTOC_PEPS_D.VALORUNITARIO AS VLRUNITARIOPROMEDIO,  
                0 AS VSALDO1,  
                 CASE WHEN (SYSDATE >=  ULTIMOMOVIMIENTOC_PEPS_D.ULTIMODEFECHASALIDA OR ULTIMOMOVIMIENTOC_PEPS_D.ULTIMODEFECHASALIDA IS NOT NULL) THEN ULTIMOMOVIMIENTOC_PEPS_D.SALDO_PEPS ELSE ULTIMOMOVIMIENTOC_PEPS_D.CSALDO END AS TCANTIDAD2
                FROM INVENTARIO INVENTARIO
                    LEFT JOIN (
                    SELECT 
                                      D_MOVIMIENTO.COMPANIA COMPANIA, 
                                      D_MOVIMIENTO.ELEMENTO ELEMENTO,
                                      MAX(D_MOVIMIENTO.TIPOMOVIMIENTO) KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS ULTIMODETIPOMOVIMIENTO,
                                      MAX(D_MOVIMIENTO.MOVIMIENTO)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS ULTIMODEMOVIMIENTO,
                                      MAX(D_MOVIMIENTO.ESTADO)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS ULTIMODEESTADO,
                                      MAX(D_MOVIMIENTO.FECHA)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS ULTIMODEFECHA,
                                      MAX(D_MOVIMIENTO.VLRUNITARIOPROM)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS VPROMEDIO, 
                                      D_MOVIMIENTO.SALDO_PEPS,
                                      D_MOVIMIENTO.VALORUNITARIO,
                                      INVENTARIO.TIPO, 
                                      SUM(CASE MOVIMIENTO.CLASE WHEN 'E' THEN D_MOVIMIENTO.SALDOKARDEX ELSE 0 END) AS CSALDO,
                                      SUM(CASE MOVIMIENTO.CLASE WHEN 'E' THEN D_MOVIMIENTO.SALDOKARDEX ELSE 0 END) * D_MOVIMIENTO.VLRUNITARIOPROM AS VSALDO,
                                      MAX(CASE MOVIMIENTO.CLASE WHEN 'S' THEN D_MOVIMIENTO.FECHA END)  KEEP (DENSE_RANK LAST ORDER BY D_MOVIMIENTO.FECHA,D_MOVIMIENTO.HORA) AS ULTIMODEFECHASALIDA
                                      FROM D_MOVIMIENTO INNER JOIN V_MOVIMIENTO MOVIMIENTO ON 
                                          D_MOVIMIENTO.COMPANIA = MOVIMIENTO.COMPANIA AND 
                                          D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO AND 
                                          D_MOVIMIENTO.MOVIMIENTO = MOVIMIENTO.NUMERO 
                                      INNER JOIN INVENTARIO ON 
                                          D_MOVIMIENTO.ELEMENTO = INVENTARIO.CODIGOELEMENTO AND 
                                          D_MOVIMIENTO.COMPANIA = INVENTARIO.COMPANIA 
                                      WHERE  D_MOVIMIENTO.COMPANIA=s$compania$s 
                                        AND D_MOVIMIENTO.ELEMENTO
                                         BETWEEN s$ELEMENTODESDE$s AND s$ELEMENTOHASTA$s 
                                        AND D_MOVIMIENTO.FECHA<=s$FECHA$s 
                                        AND D_MOVIMIENTO.IND_REG NOT IN (0)
                                        AND MOVIMIENTO.CLASE IN ('S','E')
                                      GROUP BY D_MOVIMIENTO.COMPANIA,
                                      D_MOVIMIENTO.ELEMENTO, 
                                      D_MOVIMIENTO.VLRUNITARIOPROM, 
                                      D_MOVIMIENTO.SALDO_PEPS, 
                                      D_MOVIMIENTO.VALORUNITARIO, 
                                      INVENTARIO.TIPO,
                                      D_MOVIMIENTO.FECHA,
                                      SALDOKARDEX,
                                      MOVIMIENTO.CLASE
                                      ORDER BY D_MOVIMIENTO.COMPANIA DESC,
                                       D_MOVIMIENTO.ELEMENTO DESC
                    ) ULTIMOMOVIMIENTOC_PEPS_D ON 
                  INVENTARIO.COMPANIA = ULTIMOMOVIMIENTOC_PEPS_D.COMPANIA AND
                  INVENTARIO.CODIGOELEMENTO = ULTIMOMOVIMIENTOC_PEPS_D.ELEMENTO
                  LEFT JOIN UNIDAD ON 
                  INVENTARIO.UNIDAD = UNIDAD.UNIDAD
                WHERE INVENTARIO.COMPANIA=s$compania$s 
                  AND ULTIMOMOVIMIENTOC_PEPS_D.ELEMENTO
                  BETWEEN s$ELEMENTODESDE$s AND s$ELEMENTOHASTA$s 
                  AND LENGTH(ELEMENTO) >=  s$PR_DIGITOSAGRUPACIONINV$s 
                  AND INVENTARIO.TIPO='C' 
                  AND INVENTARIO.TIENEMOVIMIENTO NOT IN (0)
                  s$SALDO2$s
