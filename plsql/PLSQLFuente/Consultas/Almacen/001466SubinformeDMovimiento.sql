SELECT COMPANIA, 
       TIPOMOVIMIENTO, 
       MOVIMIENTO, 
       SUM(VALORTOTAL) AS SUMADEVALORTOTAL, 
       LPAD(ELEMENTO,5) AS EXPR1, 
       SUM(VALORIVA) AS SUMADEVALORIVA, 
       SUM(VALORBASE) AS SUMADEVALORBASE, 
       SUM(VLRIMPCONSUMO) AS SUMADEVLRIMPCONSUMO  
  FROM D_MOVIMIENTO
 WHERE COMPANIA = $P{PR_COMPANIA}
   AND TIPOMOVIMIENTO = $P{PR_TIPOMOVIMIENTO}
   AND MOVIMIENTO BETWEEN $P{PR_MOVIMIENTO} AND $P{PR_MOVIMIENTO}
 GROUP BY COMPANIA, 
          TIPOMOVIMIENTO,
          MOVIMIENTO, 
          LPAD(ELEMENTO,5)    
