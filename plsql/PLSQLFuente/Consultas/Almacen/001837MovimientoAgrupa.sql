MERGE INTO CONSULTAS FIN USING (SELECT '001837MovimientoAgrupa' INFORME ,TO_CLOB(q'[SELECT 
  SUM(CASE WHEN TIPOMOVIMIENTO.CLASE IN ('E') THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END) ENTRADAS,
  SUM(CASE WHEN TIPOMOVIMIENTO.CLASE IN ('S') THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END) SALIDAS,
  SUM(CASE WHEN TIPOMOVIMIENTO.CLASE NOT IN ('E', 'S') THEN D_MOVIMIENTO.VALORTOTAL ELSE 0 END) AS OTROS_MOVIMIENTOS,
  TO_CHAR(D_MOVIMIENTO.FECHA, 'DD/MM/YYYY') AS FECHA,
  TIPOMOVIMIENTO.NOMBRE,
  D_MOVIMIENTO.ELEMENTO,
  SUM(D_MOVIMIENTO.CANTIDAD) AS SUMADECANTIDAD,
  D_MOVIMIENTO.TIPOMOVIMIENTO,
  D_MOVIMIENTO.MOVIMIENTO,
  SUBSTR(D_MOVIMIENTO.ELEMENTO, 0 ,s$digitos$s) GRUPO,
  INVENTARIO.NOMBRELARGO,
  GRUPO_INV.NOMBRELARGO AS NOMBGRUPO
FROM D_MOVIMIENTO
INNER JOIN TIPOMOVIMIENTO
   ON D_MOVIMIENTO.COMPANIA       = TIPOMOVIMIENTO.COMPANIA
  AND D_MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
INNER JOIN INVENTARIO
   ON D_MOVIMIENTO.COMPANIA  = INVENTARIO.COMPANIA
  AND D_MOVIMIENTO.ELEMENTO  = INVENTARIO.CODIGOELEMENTO
  INNER JOIN INVENTARIO GRUPO_INV
   ON D_MOVIMIENTO.COMPANIA = GRUPO_INV.COMPANIA
  AND SUBSTR(D_MOVIMIENTO.ELEMENTO, 0 ,s$digitos$s) = GRUPO_INV.CODIGOELEMENTO 
WHERE TO_CHAR(D_MOVIMIENTO.FECHA, 'DD/MM/YYYY') BETWEEN s$fechaInicial$s AND s$fechaFinal$s
 AND  SUBSTR(D_MOVIMIENTO.ELEMENTO,0,s$digitos$s) BETWEEN 's$agruparInicial$s' AND 's$agruparFinal$s' 
  AND D_MOVIMIENTO.IND_REG NOT IN (0)
GROUP BY TO_CHAR(D_MOVIMIENTO.FECHA, 'DD/MM/YYYY'),
  TIPOMOVIMIENTO.NOMBRE,
  D_MOVIMIENTO.ELEMENTO,
  D_MOVIMIENTO.TIPOMOVIMIENTO,
  D_MOVIMIENTO.MOVIMIENTO,
 SUBSTR(D_MOVIMIENTO.ELEMENTO, 0 ,s$digitos$s),
  INVENTARIO.NOMBRELARGO,
  GRUPO_INV.NOMBRELARGO
ORDER BY D_MOVIMIENTO.TIPOMOVIMIENTO,
  D_MOVIMIENTO.MOVIMIENTO]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);