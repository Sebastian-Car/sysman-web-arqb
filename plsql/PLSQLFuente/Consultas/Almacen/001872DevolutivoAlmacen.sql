SELECT NOMBRELARGO AS NOMBREGRUPO,TVALOR,AGRUPACION,DESCRIP,SERIEDEVOLUTIVO,NOMBRELARGOS,ELEMENTO,SERIE1 FROM(
SELECT DEVOLUTIVO.COMPANIA,
  DEVOLUTIVO.ELEMENTO,
  DEVOLUTIVO.SERIE AS SERIE1,
  DEVOLUTIVO.SERIEDEVOLUTIVO,
  DEVOLUTIVO.VALOR AS TVALOR,
  ESTADODEVOLUTIVO.DESCRIPCION AS DESCRIP,
  SUBSTR(DEVOLUTIVO.ELEMENTO,0,s$digito$s) AS AGRUPACION,
  INVENTARIO.NOMBRELARGO
  || ' '
  || DEVOLUTIVO.DESCRIPCION AS NOMBRELARGOS,
  INVENTARIO.NOMBRELARGO AS NOMBREGRUPO
FROM DEVOLUTIVO
INNER JOIN INVENTARIO
ON DEVOLUTIVO.COMPANIA  = INVENTARIO.COMPANIA
AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
INNER JOIN DEPENDENCIA
ON DEVOLUTIVO.COMPANIA     = DEPENDENCIA.COMPANIA
AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO
LEFT JOIN UNIDAD 
ON INVENTARIO.UNIDAD = UNIDAD.UNIDAD 
INNER JOIN ESTADODEVOLUTIVO
ON DEVOLUTIVO.ESTADO = ESTADODEVOLUTIVO.ESTADO
WHERE DEVOLUTIVO.COMPANIA = s$compania$s
AND DEVOLUTIVO.DEPENDENCIA  IN ('000000000000')
AND DEVOLUTIVO.PLACAANULADA IN (0)
GROUP BY DEVOLUTIVO.COMPANIA,
  DEVOLUTIVO.ELEMENTO,
  DEVOLUTIVO.SERIE,
  DEVOLUTIVO.SERIEDEVOLUTIVO,
  DEVOLUTIVO.VALOR,
  ESTADODEVOLUTIVO.DESCRIPCION,
  SUBSTR(DEVOLUTIVO.ELEMENTO,0,s$digito$s),
  INVENTARIO.NOMBRELARGO,
  DEVOLUTIVO.DESCRIPCION
) SUB
INNER JOIN INVENTARIO
ON SUB.COMPANIA  = INVENTARIO.COMPANIA
AND SUB.AGRUPACION = INVENTARIO.CODIGOELEMENTO
ORDER BY CASE WHEN  's$ordenamiento$s' = '1' THEN AGRUPACION ELSE NOMBRELARGO END
