SELECT D.SERIE_DEVOLUTIVO PLACA,
       D.ELEMENTO_DEVOLUTIVO ELEMENTO, 
       (TRIM(I.NOMBRELARGO) || CHR(32) 
       || TRIM(DEVOLUTIVO.DESCRIPCION)) DESCRIPCION,
       H.FECHA_HORA_INICIO, 
       H.FECHA_HORA_FIN, 
       T.DOCUMENTO_SOLICITANTE TERCERO,
       TERCERO.NOMBRE NOMBRE_TERCERO,
       E.NOMBRE TIPO_EVENTO
  FROM TRANSACCION_DEVOLUTIVO T 
       INNER JOIN D_TRANSACCION_DEV D 
       ON  T.COMPANIA = D.COMPANIA
       AND T.TIPO = D.TIPO_TRANSACCION
       AND T.CODIGO = D.CODIGO_TRANSACCION
       INNER JOIN HORARIO_SOLICITUD H
       ON  T.COMPANIA = H.COMPANIA
       AND T.TIPO = H.TIPO_TRANSACCION
       AND T.CODIGO = H.CODIGO_TRANSACCION
       INNER JOIN TERCERO 
       ON  T.COMPANIA = TERCERO.COMPANIA
       AND T.DOCUMENTO_SOLICITANTE = TERCERO.NIT
       AND T.SUCURSAL_SOLICITANTE = TERCERO.SUCURSAL
       INNER JOIN TIPO_EVENTO E
       ON  T.COMPANIA = E.COMPANIA
       AND T.TIPO_EVENTO = E.CODIGO
       INNER JOIN DEVOLUTIVO
       ON  D.COMPANIA = DEVOLUTIVO.COMPANIA
       AND D.ELEMENTO_DEVOLUTIVO = DEVOLUTIVO.ELEMENTO
       AND D.SERIE_DEVOLUTIVO = DEVOLUTIVO.SERIE
       INNER JOIN INVENTARIO I
       ON  D.COMPANIA = I.COMPANIA
       AND D.ELEMENTO_DEVOLUTIVO = I.CODIGOELEMENTO
 WHERE T.COMPANIA = s$compania$s
   AND T.TIPO = 'APR'
   AND T.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s 
   AND D.ELEMENTO_DEVOLUTIVO BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s'
   AND D.SERIE_DEVOLUTIVO BETWEEN s$serieInicial$s AND s$serieFinal$s
 ORDER BY D.SERIE_DEVOLUTIVO, D.ELEMENTO_DEVOLUTIVO, H.FECHA_HORA_INICIO
