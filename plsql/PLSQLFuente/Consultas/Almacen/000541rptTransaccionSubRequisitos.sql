SELECT TRANSACCION_REQUISITOS.REQUISITO,  
       REQUISITOS_PRESTAMO.NOMBRE,  
       TRANSACCION_REQUISITOS.RUTA,  
       DECODE(CUMPLE,0,'NO',-1,'SI') PCUMPLE 
FROM   REQUISITOS_PRESTAMO  
       INNER JOIN TRANSACCION_REQUISITOS  
          ON  REQUISITOS_PRESTAMO.COMPANIA = TRANSACCION_REQUISITOS.COMPANIA 
          AND REQUISITOS_PRESTAMO.CODIGO   = TRANSACCION_REQUISITOS.REQUISITO  
WHERE  TRANSACCION_REQUISITOS.COMPANIA           = $P{PR_COMPANIA} 
  AND  TRANSACCION_REQUISITOS.TIPO_TRANSACCION   = $P{PR_TIPO_TRANSACCION} 
  AND  TRANSACCION_REQUISITOS.CODIGO_TRANSACCION = $P{PR_CODIGO_TRANSACCION}
