SELECT DEPRECIAR.COMPANIA,
  DEPRECIAR.ELEMENTO,
  DEPRECIAR.SERIE,
  DEPRECIAR.PERIODO,
  INVENTARIO.NOMBRELARGO,
  DEVOLUTIVO.DESCRIPCION || CASE
            WHEN TRIM(DEVOLUTIVO.MARCA) IS NULL
            THEN ' '
            ELSE 'Marca: ' || DEVOLUTIVO.MARCA
          END NOMBRELARGOS,
  INVENTARIO.UNIDAD,
  INVENTARIO.TIPO,
  DEPRECIAR.NIIF_COSTOAJUSTADO-NIIF_VLRELEMENTO AJUSTEXINFLACIONAC,
  DEPRECIAR.NIIF_ACUMDEPAJ                            AS ACUMDEPAJFER,
  DEPRECIAR.NIIF_VLRELEMENTO                          AS VLRELEMENTO,
  DEPRECIAR.NIIF_VLRDEPRECIACION                      AS VLRDEPRECIACION,
  DEPRECIAR.NIIF_VLRACUMULADO                         AS VLRACUMULADO,
  DEPRECIAR.NIIF_SALDOXDEPRECIAR                      AS SALDOXDEPRECIAR,
  DEPRECIAR.NIIF_VLRAJUSTADO                          AS VLRAJUSTADO,
  DEPRECIAR.NIIF_VLRACUMULADOAJ                       AS VLRACUMULADOAJ,
  DEPRECIAR.NIIF_COSTOAJUSTADO                        AS COSTOAJUSTADO,
  DEPRECIAR.NIIF_VLRLIBROS                            AS VLRLIBROS,
  DEPRECIAR.IMPRIMIR,
  DEPRECIAR.NIIF_VLRAJUSTEELEM     AS VLRAJUSTEELEM,
  DEPRECIAR.NIIF_VLRACUMAJUSTEELEM AS VLRACUMAJUSTEELEM,
  DEPRECIAR.ELEMENTOBODEGA,
  DEPRECIAR.PLACAANULADA,
  DEPRECIAR.NIIF_VLRDEPRECIACIONAJ AS VLRDEPRECIACIONAJ,
  DEPRECIAR.NIIF_DEPACUMULADAAJ    AS DEPACUMULADAAJ,
  DEPRECIAR.NIIF_SALDOAJ           AS SALDOAJ,
  DEPRECIAR.NIIF_COSTOSALIDA       AS COSTOSALIDA,
  DEPRECIAR.NIIF_COSTOSALIDAAJ     AS COSTOSALIDAAJ,
  DEPRECIAR.NIIF_DEPACUMULADA      AS DEPACUMULADA,
  DEPRECIAR.DEPENDENCIA,
  DEPRECIAR.NIIF_AJDEPACUMULADA AS AJDEPACUMULADA,
  DEPRECIAR.NIIF_AJDEPRECIACION AS AJDEPRECIACION,
  DEPRECIAR.ANO,
  DEPRECIAR.MES,
  DEPRECIAR.NIIF_ACUMDEPAJ     AS ACUMDEPAJ,
  DEPRECIAR.NIIF_ACUMAJUSTESAJ AS ACUMAJUSTESAJ,
  DEVOLUTIVO.MARCA,
  DEVOLUTIVO.DESCRIPCION,
  DEVOLUTIVO.NIIF_FECHAADQUISICION                                                        AS FECHAADQUISICION,
  SUBSTR(DEPRECIAR.ELEMENTO,0, s$agrupacion$s) AGRUPACION,
  SUBSTR(DEPRECIAR.ELEMENTO,0,5) SUBGRP,
  PCK_ALMACEN_COM1.FC_DEPRECIACIONCOMODATO(DEPRECIAR.COMPANIA,'s$elementoInicial$s', 's$elementoFinal$s', s$anio$s, s$mes$s, ''''||SUBSTR(DEPRECIAR.ELEMENTO, 1, 3)||'''', 1) DEPRECIACION,
	PCK_ALMACEN.FC_NOMBREINVENTARIO(INVENTARIO.COMPANIA, SUBSTR(DEPRECIAR.ELEMENTO, 1, s$agrupacion$s),0) NOMBREINVENTARIO,
  ''                                                                                      AS CODIGOCUENTA,
  ''                                                                                      AS NOMBRECUENTA,
  DEVOLUTIVO.NIIF_FECHAADQUISICION
FROM DEPRECIAR
INNER JOIN DEVOLUTIVO
ON DEPRECIAR.SERIE     = DEVOLUTIVO.SERIE
AND DEPRECIAR.ELEMENTO = DEVOLUTIVO.ELEMENTO
AND DEPRECIAR.COMPANIA = DEVOLUTIVO.COMPANIA
LEFT JOIN INVENTARIO
ON DEPRECIAR.ELEMENTO   = INVENTARIO.CODIGOELEMENTO
AND DEPRECIAR.COMPANIA  = INVENTARIO.COMPANIA
LEFT JOIN DEPENDENCIA
ON DEPRECIAR.COMPANIA = DEPENDENCIA.COMPANIA
AND DEPRECIAR.DEPENDENCIA = DEPENDENCIA.CODIGO
INNER JOIN BODEGA
     ON DEPRECIAR.COMPANIA = BODEGA.COMPANIA
   AND DEPRECIAR.BODEGA = BODEGA.CODIGO
WHERE DEPRECIAR.COMPANIA =s$compania$s
     AND DEPRECIAR.ELEMENTO BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s'
     AND TO_CHAR(DEPRECIAR.PERIODO,'DD/MM/YYYY') = TO_DATE('s$ultimoDia$s','DD/MM/YYYY')
     AND (DEVOLUTIVO.FECHAANULADA IS NULL
      OR TO_CHAR(DEVOLUTIVO.FECHAANULADA,'DD/MM/YYYY')  > TO_DATE('s$ultimoDia$s','DD/MM/YYYY'))
    s$filtro$s
     AND TO_CHAR(DEVOLUTIVO.NIIF_FECHAADQUISICION,'DD/MM/YYYY') <= TO_DATE('s$ultimoDia$s','DD/MM/YYYY')
ORDER BY DEPRECIAR.COMPANIA,
  DEPRECIAR.ELEMENTO,
  DEPRECIAR.SERIE,
  DEPRECIAR.PERIODO
