SELECT * FROM (
  SELECT NVL(CIUDAD.NOMBRE, NVL(CIUDADDEV.NOMBRE,'SIN DEFINIR')) CIUDAD, 
  NVL(ELEMENTOSCOMPONENTES.TIPOCOMPONENTE, SUBSTR(DEVOLUTIVO.ELEMENTO,1,3)) TIPOCOMPONENTE,
  DEVOLUTIVO.ELEMENTO
  FROM DEVOLUTIVO
  INNER JOIN INVENTARIO I 
    ON DEVOLUTIVO.COMPANIA=I.COMPANIA 
    AND SUBSTR(DEVOLUTIVO.ELEMENTO,1,3)=I.CODIGOELEMENTO
  LEFT JOIN DETALLE_ELEMENTOSCOMPONENTES DE
    ON DEVOLUTIVO.COMPANIA = DE.COMPANIA 
    AND DEVOLUTIVO.ELEMENTO = DE.CODIGO_COMPONENTE 
    AND DEVOLUTIVO.SERIE = DE.SERIE_COMPONENTE 
  LEFT JOIN ELEMENTOSCOMPONENTES
    ON DE.COMPANIA         = ELEMENTOSCOMPONENTES.COMPANIA
    AND DE.CODIGO_ELEMENTO = ELEMENTOSCOMPONENTES.CONSECUTIVO
  LEFT JOIN TIPOCOMPONENTE
    ON ELEMENTOSCOMPONENTES.COMPANIA  = TIPOCOMPONENTE.COMPANIA
    AND ELEMENTOSCOMPONENTES.TIPOCOMPONENTE =  TIPOCOMPONENTE.CODIGO
  LEFT JOIN PAISES
    ON ELEMENTOSCOMPONENTES.PAIS = PAISES.PAIS
  LEFT JOIN DEPARTAMENTO
    ON ELEMENTOSCOMPONENTES.PAIS = DEPARTAMENTO.PAIS
    AND ELEMENTOSCOMPONENTES.DEPARTAMENTO = DEPARTAMENTO.CODIGO
  LEFT JOIN CIUDAD
    ON ELEMENTOSCOMPONENTES.PAIS = CIUDAD.PAIS
    AND ELEMENTOSCOMPONENTES.DEPARTAMENTO = CIUDAD.DEPARTAMENTO
    AND ELEMENTOSCOMPONENTES.CIUDAD = CIUDAD.CODIGO
  LEFT JOIN CIUDAD CIUDADDEV
    ON DEVOLUTIVO.PAIS    = CIUDADDEV.PAIS
    AND DEVOLUTIVO.DEPARTAMENTO = CIUDADDEV.DEPARTAMENTO
    AND DEVOLUTIVO.CIUDAD = CIUDADDEV.CODIGO
WHERE DEVOLUTIVO.COMPANIA  = s$compania$s)
PIVOT (COUNT(ELEMENTO) FOR TIPOCOMPONENTE 
IN (s$condicion$s))
ORDER BY CIUDAD
