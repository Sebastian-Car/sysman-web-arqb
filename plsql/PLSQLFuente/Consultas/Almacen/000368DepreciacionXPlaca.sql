MERGE INTO CONSULTAS FIN USING (SELECT '000368DepreciacionXPlaca' INFORME ,TO_CLOB(q'[SELECT  DEVOLUTIVO.COMPANIA, 
        DEVOLUTIVO.ELEMENTO, 
        DEVOLUTIVO.SERIE, 
        INVENTARIO.NOMBRELARGO || ' ' || DEVOLUTIVO.DESCRIPCION AS NOMBRE, 
        DEVOLUTIVO.VALOR, 
        DEPRECIAR.PERIODO PERIODO, 
        DEPRECIAR.VLRDEPRECIACION, 
        DEPRECIAR.DEPACUMULADA, 
        DEPRECIAR.VLRLIBROS AS SALDOXDEP, 
        DEPRECIAR.VLRELEMENTO
FROM DEVOLUTIVO 
  LEFT JOIN INVENTARIO ON DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA 
    AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO 
  LEFT JOIN DEPRECIAR ON DEVOLUTIVO.COMPANIA = DEPRECIAR.COMPANIA 
    AND DEVOLUTIVO.ELEMENTO = DEPRECIAR.ELEMENTO 
    AND DEVOLUTIVO.SERIE = DEPRECIAR.SERIE
WHERE DEVOLUTIVO.COMPANIA= s$compania$s
  AND DEVOLUTIVO.SERIE= s$seriePlaca$s]') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);