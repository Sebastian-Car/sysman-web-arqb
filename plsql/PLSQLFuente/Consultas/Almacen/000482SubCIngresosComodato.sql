SELECT  D_MOVIMIENTO.VALORTOTAL TOTAL,
        SUBSTR(D_MOVIMIENTO.ELEMENTO,0,
         (CASE WHEN PCK_SYSMAN_UTL.FC_PAR(D_MOVIMIENTO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',PCK_DATOS.FC_MODULOALMACEN,SYSDATE) = ''
                      THEN '3'
                      ELSE PCK_SYSMAN_UTL.FC_PAR(D_MOVIMIENTO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',PCK_DATOS.FC_MODULOALMACEN,SYSDATE)
                      END )) AGRUPACION,
        INVENTARIO.NOMBRELARGO
FROM D_MOVIMIENTO 
  INNER JOIN V_MOVIMIENTO MOVIMIENTO 
    ON  D_MOVIMIENTO.COMPANIA 	    = MOVIMIENTO.COMPANIA
    AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO 
    AND D_MOVIMIENTO.MOVIMIENTO     = MOVIMIENTO.NUMERO  
  INNER JOIN INVENTARIO
     ON  INVENTARIO.COMPANIA       = D_MOVIMIENTO.COMPANIA
  WHERE D_MOVIMIENTO.COMPANIA = s$compania$s 
    AND INVENTARIO.CODIGOELEMENTO = SUBSTR(D_MOVIMIENTO.ELEMENTO,0,
         (CASE WHEN PCK_SYSMAN_UTL.FC_PAR(D_MOVIMIENTO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',PCK_DATOS.FC_MODULOALMACEN,SYSDATE) = ''
                      THEN '3'
                      ELSE PCK_SYSMAN_UTL.FC_PAR(D_MOVIMIENTO.COMPANIA,'DIGITOS AGRUPACION INVENTARIO',PCK_DATOS.FC_MODULOALMACEN,SYSDATE)
                      END )) 
    AND MOVIMIENTO.CLASE        = 'E'
    AND INVENTARIO.TIPO = 'E'
    AND D_MOVIMIENTO.FECHA BETWEEN  s$fechaInicial$s
                                                            AND  s$fechaFinal$s
    AND D_MOVIMIENTO.IND_REG        NOT IN 0 
	AND MOVIMIENTO.CONCEPTO     <> ' '
