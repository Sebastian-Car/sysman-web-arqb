SELECT 
  V_MOVIMIENTO_INFORME.COMPANIA,
  V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO,
  V_MOVIMIENTO_INFORME.MOVIMIENTO NUMERO,
  NVL(V_MOVIMIENTO_INFORME.CODIGO,0) CODIGO,
  CASE WHEN V_MOVIMIENTO_INFORME.IND_REG NOT IN (0) 
    THEN ''
    ELSE 'X'
  END NOREGISTRADO,
   V_MOVIMIENTO_INFORME.NOMBRELARGO || '' || V_MOVIMIENTO_INFORME.SERIE || ''||V_MOVIMIENTO_INFORME.ESPECIFICACION ESPECIFICACION,
  V_MOVIMIENTO_INFORME.ORDENDESUMINISTRO,
  NVL(TRIM(V_MOVIMIENTO_INFORME.SERIE),' ') SERIE,
  V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO_NOM TIPOMOVIMIENTO_NOMBRE,
  V_MOVIMIENTO_INFORME.TIPOMOVASOCIADO_NOM,
  V_MOVIMIENTO_INFORME.CONCEPTO,
  V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO_NOMCORTO NOMBRECORTO,
  V_MOVIMIENTO_INFORME.NOMBRELARGO,
  V_MOVIMIENTO_INFORME.UNIDAD,
  NVL(V_MOVIMIENTO_INFORME.CANTIDAD,0) CANTIDAD,
  NVL(V_MOVIMIENTO_INFORME.VALORUNITARIO,0) VALORUNITARIO,
  NVL(V_MOVIMIENTO_INFORME.VALORTOTAL_D,0) VALORTOTAL,
  V_MOVIMIENTO_INFORME.FECHA_MOV FECHA,
  V_MOVIMIENTO_INFORME.TERCERO, 
  V_MOVIMIENTO_INFORME.TIPOMOVASOCIADO,
  V_MOVIMIENTO_INFORME.MOVASOCIADO,
  V_MOVIMIENTO_INFORME.FECHAMOVASOCIADO,
  V_MOVIMIENTO_INFORME.DEPENDENCIA_ORIGEN,
  NVL(V_MOVIMIENTO_INFORME.DESCRIPCION_MOV,' ') DESCRIPCION,
  V_MOVIMIENTO_INFORME.FACTURANO,
  V_MOVIMIENTO_INFORME.REMISIONNO,
  V_MOVIMIENTO_INFORME.VALORTOTAL_D VLRTOTAL,
  V_MOVIMIENTO_INFORME.DEPENDENCIA_ORIGEN_NOM NOMBRE_DEPENDENCIA_O,
  V_Movimiento_Informe.Dependencia_Destino_NOM NOMBRE_DEPENDENCIA_D,
  V_MOVIMIENTO_INFORME.TERCERO_NOM TERCERO_NOMBRE,
  V_MOVIMIENTO_INFORME.TERCERO_TEL TELEFONOS,
  V_MOVIMIENTO_INFORME.TERCERO_FAX FAX,
  V_MOVIMIENTO_INFORME.TIPOMOVASOCIADO_NOM DOCASOCIADO_NOMBRE,
  V_MOVIMIENTO_INFORME.RESPONSABLE_ORIGEN CEDULA_O,
  V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO CEDULA_D,
  V_MOVIMIENTO_INFORME.RESPONSABLE_ORIGEN_NOM NOMBRE_RES_O,
  V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO_NOM NOMBRE_RES_D,
  V_MOVIMIENTO_INFORME.CENTRODECOSTO_NOM NOMBRE,
  V_MOVIMIENTO_INFORME.CENTRODECOSTO MOVIMIENTO_CENTRODECOSTO,
  V_MOVIMIENTO_INFORME.ELEMENTO INVENTARIO_CODIGOELEMENTO,
  NVL(TRIM(V_MOVIMIENTO_INFORME.MARCA),' ') MARCA,
  NVL(TRIM(V_MOVIMIENTO_INFORME.SERIEDEVOLUTIVO),' ') SERIEDEVOLUTIVO,
  NVL(V_MOVIMIENTO_INFORME.VALORDOCASOCIADO,0) VALORDOCASOCIADO,
  V_MOVIMIENTO_INFORME.UNIDAD_NOM UNIDAD_NOMBRE,
  V_MOVIMIENTO_INFORME.TOTALCONAJUSTE_MOV TOTALCONAJUSTE,
  V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO||V_MOVIMIENTO_INFORME.MOVIMIENTO GRUPO,
  V_MOVIMIENTO_INFORME.TIPOPESONA,
  V_MOVIMIENTO_INFORME.AJUSTECENTAVOS1_MOV AJUSTECENTAVOS,
  V_MOVIMIENTO_INFORME.AJUSTECENTAVOS_D DETALLEAJUSTE,
  V_MOVIMIENTO_INFORME.TERCERO NIT,
  V_MOVIMIENTO_INFORME.FINICIO_CONTRATO,
  CASE WHEN V_MOVIMIENTO_INFORME.TIPOELEMENTO IN ('C') 
    THEN V_MOVIMIENTO_INFORME.NOMBRELARGO || '' || V_MOVIMIENTO_INFORME.SERIE || ''||V_MOVIMIENTO_INFORME.ESPECIFICACION 
    ELSE V_MOVIMIENTO_INFORME.NOMBRELARGO || '' || V_MOVIMIENTO_INFORME.SERIE ||  ''|| DEVOLUTIVO.DESCRIPCION 
  END  DESCRIPCION_REPORTE,
       (CASE WHEN V_MOVIMIENTO_INFORME.TIPOPESONA='R' 
          THEN V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO_NOM || ', C.C.' || V_MOVIMIENTO_INFORME.RESPONSABLE_DESTINO 
          ELSE V_MOVIMIENTO_INFORME.TERCERO_NOM || ', TEL:' || V_MOVIMIENTO_INFORME.TERCERO_TEL || ' , NIT: ' || V_MOVIMIENTO_INFORME.TERCERO END) || 
       (CASE WHEN V_MOVIMIENTO_INFORME.TERCERO_DIR IS NOT NULL 
          THEN '  DIRECCIÓN:' || V_MOVIMIENTO_INFORME.TERCERO_DIR || ' ' END) || 
       (CASE WHEN V_MOVIMIENTO_INFORME.TERCERO_FAX IS NOT NULL THEN '  FAX:' || V_MOVIMIENTO_INFORME.TERCERO_FAX END) DESCRIPCIONHEADER
FROM V_MOVIMIENTO_INFORME
LEFT JOIN DEVOLUTIVO
 ON V_MOVIMIENTO_INFORME.COMPANIA = DEVOLUTIVO.COMPANIA
AND V_MOVIMIENTO_INFORME.ELEMENTO = DEVOLUTIVO.ELEMENTO
AND V_MOVIMIENTO_INFORME.SERIE = DEVOLUTIVO.SERIE
WHERE V_MOVIMIENTO_INFORME.COMPANIA       = s$compania$s
  AND V_MOVIMIENTO_INFORME.TIPOMOVIMIENTO = 's$tipoMovimiento$s'
  AND V_MOVIMIENTO_INFORME.MOVIMIENTO BETWEEN s$movimientoInicial$s AND s$movimientoFinal$s 
ORDER BY 
  V_MOVIMIENTO_INFORME.MOVIMIENTO,
  V_MOVIMIENTO_INFORME.ELEMENTO
