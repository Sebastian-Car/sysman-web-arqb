SELECT
    INVENTARIO.COMPANIA, 
    INVENTARIO.CODIGOELEMENTO, 
    INVENTARIO.NOMBRELARGO, 
    CASE WHEN INVENTARIO.TIENEMOVIMIENTO <> 0 THEN INVENTARIO.TIPO ELSE '' END TIPOE, 
    INVENTARIO.UNIDAD, 
    INVENTARIO.ESTADOS
FROM  INVENTARIO 
WHERE INVENTARIO.COMPANIA = s$compania$s
    AND INVENTARIO.CODIGOELEMENTO BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s'
ORDER BY INVENTARIO.COMPANIA, 
         INVENTARIO.CODIGOELEMENTO
