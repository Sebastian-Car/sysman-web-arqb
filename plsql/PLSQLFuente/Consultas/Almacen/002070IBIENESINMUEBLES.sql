SELECT DEPRECIAR.COMPANIA, 
DEPRECIAR.ELEMENTO,
DEPRECIAR.SERIE,
DEPRECIAR.PERIODO,
INVENTARIO.TIPO,
NVL(FECHAANULADA,TO_DATE('31/12/3000')) FECHAANULADA,
DEPRECIAR.DEPENDENCIA,
CONCAT(CONCAT(CONCAT(INVENTARIO.NOMBRELARGO, ' '), DEVOLUTIVO.DESCRIPCION), 
CASE WHEN DEVOLUTIVO.MARCA = ' '
     THEN ' '
    ELSE ' ' || 'Marca: ' || DEVOLUTIVO.MARCA
END) NOMBRELARGOS,
DECODE(DEVOLUTIVO.APLICA_NIIF,-1,DEPRECIAR.NIIF_VLRELEMENTO,DEPRECIAR.VLRELEMENTO) VLRELEMENTO,
DECODE(DEVOLUTIVO.APLICA_NIIF,-1,DEPRECIAR.NIIF_COSTOAJUSTADO,DEPRECIAR.COSTOAJUSTADO) COSTOAJUSTADO,
DECODE(DEVOLUTIVO.APLICA_NIIF,-1,DEPRECIAR.NIIF_VLRLIBROS,DEPRECIAR.VLRLIBROS) VLRLIBROS, 
CASE  WHEN PCK_SYSMAN_UTL.FC_PAR(DEPRECIAR.COMPANIA,'MANEJA NIIF EN ALMACEN',10,DEPRECIAR.PERIODO)='SI'
             THEN DEPRECIAR.NIIF_VLRELEMENTO
             ELSE DEPRECIAR.VLRELEMENTO
             END VLRELEMENTO, 
	CASE  WHEN PCK_SYSMAN_UTL.FC_PAR(DEPRECIAR.COMPANIA,'MANEJA NIIF EN ALMACEN',10,DEPRECIAR.PERIODO)='SI'
             THEN DEPRECIAR.NIIF_COSTOAJUSTADO
             ELSE DEPRECIAR.COSTOAJUSTADO
             END COSTOAJUSTADO, 
 CASE  WHEN PCK_SYSMAN_UTL.FC_PAR(DEPRECIAR.COMPANIA,'MANEJA NIIF EN ALMACEN',10,DEPRECIAR.PERIODO)='SI'
             THEN DEPRECIAR.NIIF_VLRLIBROS
             ELSE DEPRECIAR.VLRLIBROS
             END VLRLIBROS, 
DEVOLUTIVO.AVALUO,
DEVOLUTIVO.ESCRITURA,
DEVOLUTIVO.NUMEROCATASTRAL,
DEVOLUTIVO.MATINMOBILIARIA,
DEVOLUTIVO.COD_CHIP,
DEVOLUTIVO.AREA
FROM DEPRECIAR 
LEFT JOIN DEVOLUTIVO 
ON DEPRECIAR.COMPANIA = DEVOLUTIVO.COMPANIA
AND DEPRECIAR.ELEMENTO = DEVOLUTIVO.ELEMENTO 
AND DEPRECIAR.SERIE = DEVOLUTIVO.SERIE
LEFT JOIN INVENTARIO 
ON DEPRECIAR.COMPANIA = INVENTARIO.COMPANIA 
AND DEPRECIAR.ELEMENTO = INVENTARIO.CODIGOELEMENTO
WHERE DEPRECIAR.COMPANIA = s$compania$s
AND DEPRECIAR.ELEMENTO BETWEEN s$elementoInicial$s AND s$elementoFinal$s
AND DEPRECIAR.PERIODO = LAST_DAY(TO_DATE(s$mes$s || '/' || s$ano$s, 'MM/YYYY'))
AND INVENTARIO.TIPO IN('N')
AND NVL(FECHAANULADA,TO_DATE('31/12/3000')) > LAST_DAY(TO_DATE(s$mes$s || '/' || s$ano$s, 'MM/YYYY'))
AND DEPRECIAR.DEPENDENCIA NOT IN('000000000000','999999999999','999999999902')
AND DEVOLUTIVO.FECHAADQUISICION <= LAST_DAY(TO_DATE(s$mes$s || '/' || s$ano$s, 'MM/YYYY'))
ORDER BY DEPRECIAR.COMPANIA, 
DEPRECIAR.ELEMENTO, 
DEPRECIAR.SERIE, 
Depreciar.PERIODO