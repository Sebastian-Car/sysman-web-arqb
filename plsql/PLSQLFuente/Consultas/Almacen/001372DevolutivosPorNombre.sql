MERGE INTO CONSULTAS FIN USING (SELECT '001372DevolutivosPorNombre' INFORME ,TO_CLOB(q'[SELECT DEVOLUTIVO.SERIE, 
       INVENTARIO.NOMBRELARGO, 
       TRIM(NOMBRELARGO) || ' MARCA:' || DEVOLUTIVO.MARCA || ' -' || DEVOLUTIVO.DESCRIPCION || 
       CASE WHEN TRIM(SERIEDEVOLUTIVO) = '' 
            THEN ''
            ELSE 'SERIE:' || SERIEDEVOLUTIVO
            END  NOMBRELARGOS,
       TERCERO.NOMBRE RESPONSABLE_NOMBRE, 
       DEPENDENCIA.NOMBRE DEPENDENCIA_NOMBRE, 
       DEVOLUTIVO.VALOR  VALORDEVOLUTIVO, 
       DEVOLUTIVO.SERIEANTERIOR, 
       DEVOLUTIVO.ELEMENTO  CODIGOELEMENTODEV
  FROM DEVOLUTIVO 
  LEFT JOIN INVENTARIO 
         ON DEVOLUTIVO.COMPANIA = INVENTARIO.COMPANIA
        AND DEVOLUTIVO.ELEMENTO = INVENTARIO.CODIGOELEMENTO
  LEFT JOIN DEPENDENCIA 
         ON DEVOLUTIVO.COMPANIA = DEPENDENCIA.COMPANIA
        AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO
  LEFT JOIN RESPONSABLE 
         ON DEVOLUTIVO.COMPANIA = RESPONSABLE.COMPANIA
        AND DEVOLUTIVO.RESPONSABLE = RESPONSABLE.CEDULA 
        AND DEVOLUTIVO.SUCURSAL_RESPONSABLE = RESPONSABLE.SUCURSAL
 INNER JOIN TERCERO
         ON RESPONSABLE.COMPANIA = TERCERO.COMPANIA
        AND RESPONSABLE.CEDULA   = TERCERO.NIT
        AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
 WHERE DEVOLUTIVO.COMPANIA = s$compania$s

   AND DEVOLUTIVO.PLACAANULADA = 0
   AND DEVOLUTIVO.ELEMENTO BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s'
   AND TO_CHAR(DEVOLUTIVO.FECHAADQUISICION, 'DD/MM/YYYY') < 's$fecha$s']') CONSULTA, 10 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);