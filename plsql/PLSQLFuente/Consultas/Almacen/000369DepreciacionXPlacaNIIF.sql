       DEVOLUTIVO.ELEMENTO, 
       DEVOLUTIVO.SERIE, 
       INVENTARIO.NOMBRELARGO || ' ' ||DEVOLUTIVO.DESCRIPCION AS NOMBRE, 
       DEVOLUTIVO.VALOR, 
       TO_CHAR(DEPRECIAR.PERIODO,'DD/MM/YYYY') PERIODO, 
       DEPRECIAR.VLRDEPRECIACION, 
       DEPRECIAR.DEPACUMULADA, 
       DEPRECIAR.VLRLIBROS AS SALDOPORDEP, 
       DEPRECIAR.VLRELEMENTO, 
       DEPRECIAR.NIIF_VLRDEPRECIACION, 
       DEPRECIAR.NIIF_DEPACUMULADA, 
       DEPRECIAR.NIIF_VLRLIBROS AS NIIF_SALDOXDEPRECIAR, 
       DEPRECIAR.NIIF_VLRELEMENTO, 
       DEVOLUTIVO.NIIF_VALOR_TOTAL, 
       DEVOLUTIVO.NIIF_VALOR_BASE, 
       TO_CHAR(DEPRECIAR.NIIF_FECHAFUNCIONAMIENTO,'DD/MM/YYYY') NIIF_FECHAFUNCIONAMIENTO, 
       DEVOLUTIVO.DETERIORO, 
       DEVOLUTIVO.SALVAMENTO,
       DEVOLUTIVO.NIIF_VIDA_UTIL
