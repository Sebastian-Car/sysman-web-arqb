SELECT INVENTARIO.CODIGOELEMENTO, 
       INVENTARIO.NOMBRECORTO, 
       TRIM(INVENTARIO.NOMBRELARGO) || ' ' || DEVOLUTIVO.DESCRIPCION || 
         CASE  WHEN TRIM(NVL(DEVOLUTIVO.MARCA,'')) IS NOT NULL THEN 'MARCA: ' || DEVOLUTIVO.MARCA || ' '
          ELSE ''
         END || 
         CASE  WHEN TRIM(NVL(DEVOLUTIVO.SERIEDEVOLUTIVO,'')) IS NOT NULL THEN 'SERIE: ' || DEVOLUTIVO.SERIEDEVOLUTIVO || ' ' 
          ELSE ''
       END NOMBRELARGO, 
       INVENTARIO.UNIDAD, 
       DEVOLUTIVO.SERIE, 
       DEVOLUTIVO.SERIEANTERIOR,
       DEVOLUTIVO.ORIGEN,
       DEVOLUTIVO.NUMEROORIGEN,
       TO_CHAR(DEVOLUTIVO.FECHAENTRADA,'DD/MM/YYYY') FECHAENTRADA,
       TO_CHAR(DEVOLUTIVO.FECHAADQUISICION,'DD/MM/YYYY') FECHAADQUISICION,
       TO_CHAR(DEVOLUTIVO.FECHASALIDASERVICIO,'DD/MM/YYYY') FECHASALIDASERVICIO,
       DEVOLUTIVO.DEPENDENCIA,
       DEPENDENCIA.NOMBRE NOMBREDEPENDENCIA, 
       ESTADODEVOLUTIVO.DESCRIPCION ESTADO,
       DEVOLUTIVO.RESPONSABLE,
       TERCERO.NOMBRE NOMBRERESPONSABLE, 
       INVENTARIO.COMPANIA, 
       RPAD(INVENTARIO.CODIGOELEMENTO,s$digitosAgrup$s,'00') AS GRUPO, 
       DEVOLUTIVO.SERIEANTERIOR, 
       DEVOLUTIVO.FECHASALIDASERVICIO,
       DEVOLUTIVO.VALOR AS VALORTOTAL, 
       TRIM(SERIEDEVOLUTIVO) AS EXPR1
FROM INVENTARIO 
  INNER JOIN (DEVOLUTIVO LEFT JOIN DEPENDENCIA ON DEVOLUTIVO.COMPANIA = DEPENDENCIA.COMPANIA AND DEVOLUTIVO.DEPENDENCIA = DEPENDENCIA.CODIGO 
  LEFT JOIN RESPONSABLE ON DEVOLUTIVO.COMPANIA = RESPONSABLE.COMPANIA AND DEVOLUTIVO.RESPONSABLE = RESPONSABLE.CEDULA AND DEVOLUTIVO.SUCURSAL_RESPONSABLE = RESPONSABLE.SUCURSAL
  LEFT JOIN TIPOMOVIMIENTO ON DEVOLUTIVO.COMPANIA = TIPOMOVIMIENTO.COMPANIA AND DEVOLUTIVO.TIPOMOVIMIENTOI = TIPOMOVIMIENTO.CODIGO
  LEFT JOIN TERCERO ON DEVOLUTIVO.COMPANIA = TERCERO.COMPANIA AND DEVOLUTIVO.RESPONSABLE = TERCERO.NIT AND DEVOLUTIVO.SUCURSAL_RESPONSABLE = TERCERO.SUCURSAL
  INNER JOIN ESTADODEVOLUTIVO  ON DEVOLUTIVO.ESTADO = ESTADODEVOLUTIVO.ESTADO)
  ON INVENTARIO.COMPANIA = DEVOLUTIVO.COMPANIA AND INVENTARIO.CODIGOELEMENTO = DEVOLUTIVO.ELEMENTO
WHERE INVENTARIO.COMPANIA = s$compania$s 
  AND INVENTARIO.CODIGOELEMENTO BETWEEN 's$elementoInicial$s' AND 's$elementoFinal$s'
  AND DEVOLUTIVO.FECHAENTRADA BETWEEN TO_DATE(s$fechaInicial$s,'DD/MM/YYYY') AND  TO_DATE(s$fechaFinal$s,'DD/MM/YYYY')
  AND DEVOLUTIVO.PLACAANULADA IN(0)
  AND TIPOMOVIMIENTO.CONCEPTO='II' 
  AND   DEVOLUTIVO.ORIGEN IN ('EDI')
  AND DEVOLUTIVO.RESPONSABLE BETWEEN 's$responsableInicial$s' AND 's$responsableFinal$s'
ORDER BY INVENTARIO.COMPANIA, INVENTARIO.CODIGOELEMENTO