SELECT 
      INVENTARIO.COMPANIA, 
      INVENTARIO.CODIGOELEMENTO, 
      INVENTARIO.NOMBRELARGO, 
      INVENTARIO.UNIDAD, 
      SUBSTR(INVENTARIO.CODIGOELEMENTO,0,1) GRUP, 
      SUBSTR(INVENTARIO.CODIGOELEMENTO,0,3) AGRUP, 
      SUBSTR(INVENTARIO.CODIGOELEMENTO,0,5) AAGRUP,
      PCK_GENERALES.FC_NOMBREINVENTARIO(SUBSTR(INVENTARIO.CODIGOELEMENTO,0,1),INVENTARIO.COMPANIA,NULL) NOMBREGRUP,
      PCK_GENERALES.FC_NOMBREINVENTARIO(SUBSTR(INVENTARIO.CODIGOELEMENTO,0,3),INVENTARIO.COMPANIA,NULL) NOMBREAGRUP,
      PCK_GENERALES.FC_NOMBREINVENTARIO(SUBSTR(INVENTARIO.CODIGOELEMENTO,0,5),INVENTARIO.COMPANIA,NULL) NOMBREAAGRUP,
      PCK_GENERALES.FC_NOMBREINVENTARIO(SUBSTR(INVENTARIO.CODIGOELEMENTO,0,1),INVENTARIO.COMPANIA,1) UNIDADGRUP
FROM   INVENTARIO 
WHERE  INVENTARIO.COMPANIA = s$compania$s
  AND INVENTARIO.CODIGOELEMENTO >= 's$elementoInicial$s' AND INVENTARIO.CODIGOELEMENTO <= 's$elementoFinal$s' 
  AND LENGTH(CODIGOELEMENTO) >= 8
ORDER BY SUBSTR(INVENTARIO.CODIGOELEMENTO,0,3)
