     FROM V_PLAN_CONTABLE P
LEFT JOIN (SELECT COM.COMPANIA, COM.ANO, DET.ID, 
                  (CASE WHEN DET.FECHA >= TO_DATE('s$fechaInicial$s','DD/MM/YYYY') 
                            AND DET.FECHA <= TO_DATE('s$fechaFinal$s','DD/MM/YYYY') 
                      THEN   DET.VALOR_DEBITO ELSE 0 END) ENTRADAS,    
                  (CASE WHEN DET.FECHA >= TO_DATE('s$fechaInicial$s','DD/MM/YYYY') 
                            AND DET.FECHA <= TO_DATE('s$fechaFinal$s','DD/MM/YYYY') 
                      THEN   DET.VALOR_CREDITO ELSE 0 END) SALIDAS, 
                  (CASE WHEN DET.FECHA >= TO_DATE('01/' || TO_CHAR(TO_DATE('s$fechaInicial$s','DD/MM/YYYY'),'MM') || '/' || TO_CHAR(TO_DATE('s$fechaInicial$s','DD/MM/YYYY'),'YYYY'), 'DD/MM/YYYY') 
                            AND DET.FECHA <  TO_DATE('s$fechaInicial$s','DD/MM/YYYY') 
                      THEN  CASE WHEN DET.NATURALEZA='D' THEN DET.VALOR_DEBITO-DET.VALOR_CREDITO  
                                 ELSE DET.VALOR_CREDITO - DET.VALOR_DEBITO  END 
                      ELSE 0 END) SALDOINI, 
                  (CASE WHEN DET.FECHA > TO_DATE('s$fechaFinal$s','DD/MM/YYYY')  
                            AND DET.FECHA <= LAST_DAY(TO_DATE('s$fechaFinal$s','DD/MM/YYYY'))
                      THEN  CASE WHEN DET.NATURALEZA='D' THEN DET.VALOR_DEBITO-DET.VALOR_CREDITO  
                                 ELSE DET.VALOR_CREDITO - DET.VALOR_DEBITO  END 
                      ELSE 0 END) SALDOFIN,     
                  COM.NRO_DOCUMENTO
