       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaFinal$s','DD/MM/YYYY'))=10 THEN SALDO10 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaFinal$s','DD/MM/YYYY'))=11 THEN SALDO11 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaFinal$s','DD/MM/YYYY'))=12 THEN SALDO12 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaFinal$s','DD/MM/YYYY'))=13 THEN SALDO13 ELSE
       0 END END END END END END END END END END END END END
