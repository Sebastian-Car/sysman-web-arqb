       MIN(CASE WHEN  MOVIMIENTO+MAN_CEN_CTO+ MAN_AUX_TER+MAN_AUX_GEN+ MAN_AUX_FUE+  MAN_AUX_REF<>0 THEN  NVL(D.NRO_DOCUMENTO,'') ELSE '' END) CHEQUEINI,
