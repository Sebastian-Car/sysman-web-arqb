            AND COM.ANO       = DET.ANO
            AND COM.TIPO      = DET.TIPO_CPTE
            AND COM.NUMERO    = DET.COMPROBANTE
           WHERE COM.COMPANIA = 's$compania$s'
		         AND COM.ANO      = s$anio$s
             AND TRUNC(COM.FECHA) BETWEEN TO_DATE('01/' || TO_CHAR(TO_DATE('s$fechaInicial$s','DD/MM/YYYY'),'MM') || '/' || TO_CHAR(TO_DATE('s$fechaInicial$s','DD/MM/YYYY'),'YYYY'), 'DD/MM/YYYY') 
                                      AND LAST_DAY(TO_DATE('s$fechaFinal$s','DD/MM/YYYY'))
          ) D
  ON P.COMPANIA = D.COMPANIA
 AND P.ANO      = D.ANO
 AND D.ID       LIKE P.ID || '%'
WHERE P.COMPANIA = 's$compania$s'
  AND P.ANO      = s$anio$s
  AND P.ID       BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
GROUP BY P.COMPANIA,
       P.ANO,
       P.ID,
       P.NOMBRE,
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaInicial$s','DD/MM/YYYY'))=1 THEN SALDO0 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaInicial$s','DD/MM/YYYY'))=2 THEN SALDO1 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaInicial$s','DD/MM/YYYY'))=3 THEN SALDO2 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaInicial$s','DD/MM/YYYY'))=4 THEN SALDO3 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaInicial$s','DD/MM/YYYY'))=5 THEN SALDO4 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaInicial$s','DD/MM/YYYY'))=6 THEN SALDO5 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaInicial$s','DD/MM/YYYY'))=7 THEN SALDO6 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaInicial$s','DD/MM/YYYY'))=8 THEN SALDO7 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaInicial$s','DD/MM/YYYY'))=9 THEN SALDO8 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaInicial$s','DD/MM/YYYY'))=10 THEN SALDO9 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaInicial$s','DD/MM/YYYY'))=11 THEN SALDO10 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaInicial$s','DD/MM/YYYY'))=12 THEN SALDO11 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaInicial$s','DD/MM/YYYY'))=13 THEN SALDO12 ELSE
       0 END END END END END END END END END END END END END ,
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaFinal$s','DD/MM/YYYY'))=1 THEN SALDO1 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaFinal$s','DD/MM/YYYY'))=2 THEN SALDO2 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaFinal$s','DD/MM/YYYY'))=3 THEN SALDO3 ELSE
       CASE WHEN EXTRACT(MONTH FROM TO_DATE('s$fechaFinal$s','DD/MM/YYYY'))=4 THEN SALDO4 ELSE
