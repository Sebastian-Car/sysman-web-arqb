SELECT 
	INVENTARIO.CODIGOELEMENTO,
	ACUMULADO.SALDO AS SALDOB,
	INVENTARIO.NOMBRELARGO,
	ACUMULADO.ANO,
	ACUMULADO.MES,
	ACUMULADO.VLRSALDOBODEGA AS VLRSALDOB,  
	(CASE WHEN SALDOBODEGA <>0 THEN VLRSALDOBODEGA/SALDOBODEGA ELSE 0 END) VLRUNITARIOFIN,
	--SUBSTR(INVENTARIO.CODIGOELEMENTO,0,s$digitos$s)  AGRUPACION,
	ACUMULADO.SALDOBODEGA
FROM INVENTARIO 
LEFT JOIN ACUMULADO 
	ON INVENTARIO.COMPANIA = ACUMULADO.COMPANIA
	AND INVENTARIO.CODIGOELEMENTO = ACUMULADO.CODIGOELEMENTO
WHERE INVENTARIO.COMPANIA ='s$compania$s'
	AND INVENTARIO.CODIGOELEMENTO BETWEEN s$elementoInicial$s AND s$elementoFinal$s
ORDER BY INVENTARIO.CODIGOELEMENTO