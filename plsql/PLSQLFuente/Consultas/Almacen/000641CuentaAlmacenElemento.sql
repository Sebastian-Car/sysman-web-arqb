SELECT  
  DECODE(CUENTAALMACENCANTIDADESDET.TIPO,'C','Consumo','D','Devolutivos','N','ActivosFijos','M','Consumo Controlado','E','En Comodato','S','Servicios') TIPO, 
  CUENTAALMACENCANTIDADESDET.NOMBREBODEGA DEPEND, 
  CUENTAALMACENCANTIDADESDET.NOMBREELEMENTO NOMBRELARGO, 
  CUENTAALMACENCANTIDADESDET.COMPANIA, 
  CUENTAALMACENCANTIDADESDET.ELEMENTO, 
  CUENTAALMACENCANTIDADESDET.ELEMENTO GRUPO, 
  CUENTAALMACENCANTIDADESDET.SERIE, 
  CUENTAALMACENCANTIDADESDET.SUMADECANTIDADENTRADAS, 
  CUENTAALMACENCANTIDADESDET.SUMADECANTIDADSALIDAS, 
  CUENTAALMACENCANTIDADESDET.SUMADEENTRADAS, 
  CUENTAALMACENCANTIDADESDET.SALIDAS1, 
  CUENTAALMACENCANTIDADESDET.SUMADESALDOANTERIOR, 
  CUENTAALMACENCANTIDADESDET.SUMADECANTIDADSALDOANTERIOR, 
  CUENTAALMACENCANTIDADESDET.SUMADESALDOFINAL, 
  CUENTAALMACENCANTIDADESDET.SUMADECANTIDADTOTAL, 
  CUENTAALMACENCANTIDADESDET.BODEGA_DESTINO1, 
  UNIDAD.NOMBRE UNIDAD 
FROM (SELECT 
        COMPANIA, 
        ELEMENTO, 
        NOMBREELEMENTO, 
        TIPO, 
        SERIE, 
        UNIDAD, 
        NOMBREBODEGA, 
        BODEGA_DESTINO1, 
        SUM(CASE WHEN V_BASE_CUENTAALMACEN.FECHA >= TO_DATE('s$fechaInicial$s','DD/MM/YYYY') THEN ENTRADAS ELSE 0 END) SUMADECANTIDADENTRADAS, 
        SUM(CASE WHEN V_BASE_CUENTAALMACEN.FECHA >= TO_DATE('s$fechaInicial$s','DD/MM/YYYY') THEN CASE WHEN CLASE='S' THEN CASE WHEN TIPO='D' THEN 1 ELSE SALIDAS END ELSE 0 END ELSE 0 END) SUMADECANTIDADSALIDAS, 
        SUM(CASE WHEN V_BASE_CUENTAALMACEN.FECHA >= TO_DATE('s$fechaInicial$s','DD/MM/YYYY') THEN VALOR_ENTRADAS ELSE 0 END) SUMADEENTRADAS, 
        SUM(CASE WHEN V_BASE_CUENTAALMACEN.FECHA >= TO_DATE('s$fechaInicial$s','DD/MM/YYYY') THEN VALOR_SALIDAS ELSE 0 END) SALIDAS1,   
        SUM(CASE WHEN V_BASE_CUENTAALMACEN.FECHA < TO_DATE('s$fechaInicial$s','DD/MM/YYYY') THEN NVL(VALOR_ENTRADAS,0)-NVL(VALOR_SALIDAS,0)+NVL(AJUSTESDEBITO,0)+NVL(AJUSTESCREDITO,0) ELSE 0 END) SUMADESALDOANTERIOR, 
        SUM(CASE WHEN V_BASE_CUENTAALMACEN.FECHA < TO_DATE('s$fechaInicial$s','DD/MM/YYYY') THEN NVL(ENTRADAS,0)-NVL(SALIDAS,0)+NVL(CANTIDADAJUSTESDEBITO,0)+NVL(CANTIDADAJUSTESCREDITO,0) ELSE 0 END) SUMADECANTIDADSALDOANTERIOR, 
        SUM(NVL(VALOR_ENTRADAS,0)-NVL(VALOR_SALIDAS,0)+NVL(AJUSTESDEBITO,0)+NVL(AJUSTESCREDITO,0)) SUMADESALDOFINAL, 
        SUM(NVL(ENTRADAS,0)-NVL(SALIDAS,0)+NVL(CANTIDADAJUSTESDEBITO,0)+NVL(CANTIDADAJUSTESCREDITO,0)) SUMADECANTIDADTOTAL 
      FROM  
        V_BASE_CUENTAALMACEN 
      WHERE COMPANIA='s$companiaSel$s' 
        AND FECHA <= TO_DATE('s$fechaFinal$s','DD/MM/YYYY')          
      GROUP BY  COMPANIA, ELEMENTO, NOMBREELEMENTO, TIPO, SERIE, UNIDAD, NOMBREBODEGA,BODEGA_DESTINO1
) CUENTAALMACENCANTIDADESDET  INNER JOIN BODEGA  
  ON CUENTAALMACENCANTIDADESDET.COMPANIA        = BODEGA.COMPANIA 
 AND CUENTAALMACENCANTIDADESDET.BODEGA_DESTINO1 = BODEGA.CODIGO  
LEFT JOIN UNIDAD  
  ON CUENTAALMACENCANTIDADESDET.UNIDAD = UNIDAD.UNIDAD
WHERE  MOSTRAR NOT IN (0)
