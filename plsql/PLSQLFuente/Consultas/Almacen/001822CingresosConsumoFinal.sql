SELECT C_INGRESOSCONSUMODEP.COMPANIA,
  C_INGRESOSCONSUMODEP.TIPOMOVIMIENTO,
  SUM(C_INGRESOSCONSUMODEP.TOTAL) TOTAL,
  C_INGRESOSCONSUMODEP.NOMBRECORTO,
  C_INGRESOSCONSUMODEP.AGRUPACION,
  INVENTARIO.NOMBRELARGO,
  C_INGRESOSCONSUMODEP.NOMBRE DEPENDENCIA
FROM (SELECT D_MOVIMIENTO.COMPANIA,
    D_MOVIMIENTO.TIPOMOVIMIENTO,
    TIPOMOVIMIENTO.CLASE,
    TIPOMOVIMIENTO.TIPOELEMENTO,
    SUM(D_MOVIMIENTO.VALORTOTAL) TOTAL,
    TIPOMOVIMIENTO.NOMBRECORTO ,
    SUBSTR(D_MOVIMIENTO.ELEMENTO, 0, s$digitos$s) AGRUPACION,
    D_MOVIMIENTO.FECHA,
    MOVIMIENTO.DEPENDENCIA_DESTINO DEPENDENCIA,
    DEPENDENCIA.NOMBRE ,
    TIPOMOVIMIENTO.CONCEPTO
  FROM D_MOVIMIENTO
  LEFT JOIN TIPOMOVIMIENTO
  ON D_MOVIMIENTO.COMPANIA        = TIPOMOVIMIENTO.COMPANIA
  AND D_MOVIMIENTO.TIPOMOVIMIENTO = TIPOMOVIMIENTO.CODIGO
  LEFT JOIN MOVIMIENTO
  ON D_MOVIMIENTO.COMPANIA        = MOVIMIENTO.COMPANIA
  AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO
  AND D_MOVIMIENTO.MOVIMIENTO     = MOVIMIENTO.NUMERO
  LEFT JOIN DEPENDENCIA
  ON MOVIMIENTO.COMPANIA             = DEPENDENCIA.COMPANIA
  AND MOVIMIENTO.DEPENDENCIA_DESTINO = DEPENDENCIA.CODIGO
  WHERE D_MOVIMIENTO.COMPANIA        = 's$compania$s'
  AND TIPOMOVIMIENTO.CLASE           = 's$clase$s'
  AND TIPOMOVIMIENTO.TIPOELEMENTO    = 's$tipo$s'
  AND D_MOVIMIENTO.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND D_MOVIMIENTO.IND_REG NOT IN (0)
  GROUP BY D_MOVIMIENTO.COMPANIA,
    D_MOVIMIENTO.TIPOMOVIMIENTO,
    TIPOMOVIMIENTO.CLASE,
    TIPOMOVIMIENTO.TIPOELEMENTO,
    TIPOMOVIMIENTO.NOMBRECORTO,
    SUBSTR(D_MOVIMIENTO.ELEMENTO, 0, s$digitos$s),
    D_MOVIMIENTO.FECHA,
    MOVIMIENTO.DEPENDENCIA_DESTINO,
    DEPENDENCIA.NOMBRE,
    TIPOMOVIMIENTO.CONCEPTO,
    D_MOVIMIENTO.IND_REG
  ) C_INGRESOSCONSUMODEP
INNER JOIN INVENTARIO
ON C_INGRESOSCONSUMODEP.COMPANIA    = INVENTARIO.COMPANIA
AND C_INGRESOSCONSUMODEP.AGRUPACION = INVENTARIO.CODIGOELEMENTO
GROUP BY C_INGRESOSCONSUMODEP.COMPANIA,
  C_INGRESOSCONSUMODEP.TIPOMOVIMIENTO,
  C_INGRESOSCONSUMODEP.NOMBRECORTO,
  C_INGRESOSCONSUMODEP.AGRUPACION,
  INVENTARIO.NOMBRELARGO,
  C_INGRESOSCONSUMODEP.DEPENDENCIA,
  C_INGRESOSCONSUMODEP.NOMBRE
