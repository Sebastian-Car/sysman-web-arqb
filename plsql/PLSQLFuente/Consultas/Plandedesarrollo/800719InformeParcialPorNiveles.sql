MERGE INTO CONSULTAS FIN USING (SELECT '800719InformeParcialPorNiveles' INFORME ,TO_CLOB(q'[   WITH ENCABEZADO AS (SELECT PT.COMPANIA,   PT.TIPO,   PT.NUMERO AS NUM_MOVIMIENTO, PT.ANO, 
TO_CHAR(PT.FECHA, 'DD/MM/YYYY') AS FECHA_MOVIMIENTO,    
   PT.DEPENDENCIA || ' ' || DEPENDENCIA.NOMBRE NOMBREDEPENDENCIA,    PT.RESPONSABLE || ' ' || TERCERO.NOMBRE NOMBRERESPONSABLE,
   PTT.CODIGO || ' ' || PTT.NOMBRE  TIPOMOVIMIENTO
FROM  PI_TRANSACCION  PT 
INNER JOIN DEPENDENCIA     ON PT.COMPANIA = DEPENDENCIA.COMPANIA     AND PT.DEPENDENCIA = DEPENDENCIA.CODIGO   
INNER JOIN TERCERO     ON PT.COMPANIA = TERCERO.COMPANIA     AND PT.RESPONSABLE = TERCERO.NIT      
AND PT.SUCURSAL = TERCERO.SUCURSAL 
INNER JOIN PI_TIPO_TRANSACCION PTT ON PT.COMPANIA = PTT.COMPANIA     AND PT.DESCRIPCION = PTT.CODIGO 
WHERE PT.COMPANIA  = s$COMPANIA$s 
	AND PT.ANO        = s$VIGENCIA$s 
	AND PT.ESTADO      = 'A' 
	AND PT.NUMERO = s$NUMERO$s 
	AND PT.TIPO        = 's$TIPO$s')
SELECT 
ENCABEZADO.TIPO, 
ENCABEZADO.NUM_MOVIMIENTO, 
ENCABEZADO.FECHA_MOVIMIENTO, 
ENCABEZADO.NOMBREDEPENDENCIA, 
ENCABEZADO.NOMBRERESPONSABLE, 
ENCABEZADO.TIPOMOVIMIENTO,
TO_CHAR(METAS.ID_PLAN) CODIGO,NVL(PLAN.DESCRIPCION,'SIN NOMBRE') DESCRIPCION,
ROUND(SUM(METAS.VALOR_PRESUPUESTO_FIN),2) RECURSO_PROGRAMADO,
ROUND(SUM(METAS.VALOR_COMPROMETIDO_FIN),2)RECURSO_COMPROMETIDO,
    ROUND(SUM(METAS.VALOR_COMPROMETIDO_FIN)/SUM(CASE WHEN METAS.VALOR_PRESUPUESTO_FIN=0 THEN 1 ELSE METAS.VALOR_PRESUPUESTO_FIN END)* 100,2 ) EJECUCION,
 OBS_CONCAT.JUSTIFICACION
FROM PI_PLAN_INDICATIVO PLAN
INNER JOIN PI_PLAN_INDICATIVO_METAS METAS
  ON  PLAN.COMPANIA         = METAS.COMPANIA
  AND PLAN.TIPO             = METAS.TIPO
  AND PLAN.NUMERO           = METAS.NUMERO
  AND PLAN.ID_PLAN          = METAS.ID_PLAN
  AND PLAN.VIGENCIA_INICIAL = METAS.VIGENCIA_PLAN
  LEFT JOIN (
    SELECT 
        O.COMPANIA,
        O.VIGENCIA_INICIAL,
        O.ID_PLAN,
        RTRIM(
            XMLAGG(
                XMLELEMENT(E, TO_CHAR(O.FECHA,'DD/MM/YYYY') || ' ' || O.OBSERVACION || CHR(10))
                ORDER BY O.FECHA
            ).EXTRACT('//text()'),
        CHR(10)) AS JUSTIFICACION
    FROM PI_PLAN_INDICATIVO_OBS O
    GROUP BY O.COMPANIA, O.VIGENCIA_INICIAL, O.ID_PLAN
) OBS_CONCAT
  ON PLAN.COMPANIA          = OBS_CONCAT.COMPANIA
  AND PLAN.VIGENCIA_INICIAL = OBS_CONCAT.VIGENCIA_INICIAL
  AND PLAN.ID_PLAN          = OBS_CONCAT.ID_PLAN
LEFT JOIN ENCABEZADO 
ON ENCABEZADO.COMPANIA = PLAN.COMPANIA
AND ENCABEZADO.TIPO = PLAN.TIPO
AND ENCABEZADO.NUM_MOVIMIENTO = PLAN.NUMERO
AND ENCABEZADO.ANO = PLAN.VIGENCIA_INICIAL
WHERE METAS.COMPANIA       = s$compania$s
  	AND METAS.VIGENCIA_PLAN = s$VIGENCIA$s
    	AND METAS.NUMERO           = s$NUMERO$s
  	AND LENGTH(TRIM(METAS.id_plan)) <= s$NDIGITOS$s
	AND METAS.TIPO             = 's$TIPO$s'
GROUP  BY TO_CHAR(METAS.ID_PLAN), METAS.ID_PLAN, NVL(PLAN.DESCRIPCION,'SIN NOMBRE'), PLAN.DESCRIPCION, 'SIN NOMBRE', 
OBS_CONCAT.JUSTIFICACION, ENCABEZADO.TIPO, ENCABEZADO.NUM_MOVIMIENTO, ENCABEZADO.TIPOMOVIMIENTO,
ENCABEZADO.FECHA_MOVI]') || TO_CLOB(q'[MIENTO, ENCABEZADO.NOMBREDEPENDENCIA, ENCABEZADO.NOMBRERESPONSABLE
ORDER BY CODIGO]') CONSULTA, 67 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
COMMIT;