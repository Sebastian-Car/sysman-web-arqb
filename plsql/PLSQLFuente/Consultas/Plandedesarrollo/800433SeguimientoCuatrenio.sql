SELECT
	C.VIGENCIA_INICIAL,
	C.CONSECUTIVO,
	C.ID,
	C.INICIATIVA_ESTRATEGICA,
	C.SECTOR_INVERSION,
	C.PROGRAMA,
	C.META_PRODUCTO,
	C.DEPENDENCIA,
	C.DESCRIPCION,
	C.META,
	C.MET_IND,
	C.META_FISICA_ESPERADA,
	C.META_FISICA_EJECUTADA,
	CASE
		C.TIPO_META_INDICADOR
		WHEN 'MIC' THEN
		CASE
			C.META_FISICA_ESPERADA WHEN 0 THEN 0
			ELSE ROUND((C.META_FISICA_EJECUTADA - C.LINEA_BASE) / (C.META_FISICA_ESPERADA - C.LINEA_BASE), 4)
		END
		WHEN 'MR' THEN
		CASE
			C.META_FISICA_ESPERADA WHEN 0 THEN 0
			ELSE ROUND((C.LINEA_BASE - C.META_FISICA_EJECUTADA) / (C.LINEA_BASE - C.META_FISICA_ESPERADA), 4)
		END
		ELSE
		CASE
			C.META_FISICA_ESPERADA WHEN 0 THEN 0
			ELSE ROUND(C.META_FISICA_EJECUTADA / C.META_FISICA_ESPERADA, 4)
		END
	END PORCENTAJE_EJECUCION,
	C.META_FINANCIERA,
	C.COMPROMETIDO_VIGENCIA,
	CASE
		C.META_FINANCIERA WHEN 0 THEN 0
		ELSE ROUND(C.COMPROMETIDO_VIGENCIA / C.META_FINANCIERA, 4)
	END AVANCE_COMPROMETIDO,
	C.PAGADO_VIGENCIA,
	CASE
		C.META_FINANCIERA WHEN 0 THEN 0
		ELSE ROUND(C.PAGADO_VIGENCIA / C.META_FINANCIERA, 4)
	END AVANCE_PAGADO
FROM
	(
	SELECT
		CO.VIGENCIA_INICIAL,
			ROW_NUMBER() OVER (
		ORDER BY
			CO.ID) AS CONSECUTIVO,
		CO.ID,
		CO.INICIATIVA_ESTRATEGICA,
		CO.SECTOR_INVERSION,
		CO.PROGRAMA,
		CO.META_PRODUCTO,
		CO.DEPENDENCIA,
		CO.DESCRIPCION,
		CO.META,
		CO.MET_IND,
		SUM(CO.U_META_FISICA_ESPERADA) META_FISICA_ESPERADA,
		SUM(CO.U_META_FISICA_EJECUTADA) META_FISICA_EJECUTADA,
		SUM(CO.U_META_FINANCIERA) META_FINANCIERA,
		SUM(CO.U_COMPROMETIDO_VIGENCIA) COMPROMETIDO_VIGENCIA,
		SUM(CO.U_PAGADO_VIGENCIA) PAGADO_VIGENCIA,
		CO.TIPO_META_INDICADOR,
		CO.LINEA_BASE
	FROM
		(
		SELECT
			BP_PLAN_INDICATIVO.VIGENCIA_INICIAL,
			BP_PLAN_INDICATIVO.ID,
			DESCRIPCION_INICIATIVA.DESCRIPCION INICIATIVA_ESTRATEGICA,
			SECTORES.DESCRIPCION SECTOR_INVERSION,
			DESCRIPCION_PROGRAMA.DESCRIPCION PROGRAMA,
			DESCRIPCION_META_PRODUCTO.DESCRIPCION META_PRODUCTO,
			DEPENDENCIA.NOMBRE DEPENDENCIA,
			BP_PLAN_INDICATIVO.DESCRIPCION,
			BP_PLAN_INDICATIVO.META,
			(CASE
				WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR = 'MI' THEN 'Incremento Acumulado'
				ELSE
				(CASE
					WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR = 'MM' THEN 'Mantenimiento'
					ELSE
					(CASE
						WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR = 'MR' THEN 'Reducción'
						ELSE
						(CASE
							WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR = 'MG' THEN 'Gestion'
							ELSE
							(CASE
								WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR = 'MIF' THEN 'Incremento Flujo'
								ELSE
								(CASE
									WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR = 'MIC' THEN 'Incremento Capacidad'
									ELSE
									(CASE
										WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR = 'MRA' THEN 'Reducción Anual'
										ELSE ' '
									END)
								END)
							END)
						END)
					END)
				END)
			END) MET_IND,
			PI_INDICADOR_META.META U_META_FISICA_ESPERADA,
			PI_INDICADOR_META.AVANCE U_META_FISICA_EJECUTADA,
			BP_PLAN_INDICATIVO_METAS.PRESUPUESTO U_META_FINANCIERA,
			BP_PLAN_INDICATIVO_METAS.COMPROMETIDO U_COMPROMETIDO_VIGENCIA,
			BP_PLAN_INDICATIVO_METAS.PAGADO U_PAGADO_VIGENCIA,
			BP_PLAN_INDICATIVO.TIPO_META_INDICADOR,
			PI_INDICADOR.LB LINEA_BASE
		FROM
			BP_PLAN_INDICATIVO
		LEFT JOIN SECTORES
     ON
			BP_PLAN_INDICATIVO.COMPANIA = SECTORES.COMPANIA
			AND BP_PLAN_INDICATIVO.SECTOR = SECTORES.CODIGO
		LEFT JOIN DEPENDENCIA
    ON
			BP_PLAN_INDICATIVO.COMPANIA = DEPENDENCIA.COMPANIA
			AND BP_PLAN_INDICATIVO.DEPENDENCIA = DEPENDENCIA.CODIGO
		LEFT JOIN PI_INDICADOR_META
              ON
			BP_PLAN_INDICATIVO.COMPANIA = PI_INDICADOR_META.COMPANIA
			AND BP_PLAN_INDICATIVO.ID = PI_INDICADOR_META.ID_PLAN
			AND BP_PLAN_INDICATIVO.VIGENCIA_INICIAL = PI_INDICADOR_META.VIGENCIA_INICIAL
		INNER JOIN PI_INDICADOR 
	ON
			PI_INDICADOR_META.COMPANIA = PI_INDICADOR.COMPANIA
			AND PI_INDICADOR_META.VIGENCIA_INICIAL = PI_INDICADOR.VIGENCIA_INICIAL
			AND PI_INDICADOR_META.ID_PLAN = PI_INDICADOR.ID_PLAN
			AND PI_INDICADOR_META.INDICADOR = PI_INDICADOR.CODIGO
		INNER JOIN BP_PLAN_INDICATIVO DESCRIPCION_INICIATIVA
              ON
			BP_PLAN_INDICATIVO.COMPANIA = DESCRIPCION_INICIATIVA.COMPANIA
			AND SUBSTR(BP_PLAN_INDICATIVO.ID, 0, PCK_SYSMAN_UTL.FC_PAR(BP_PLAN_INDICATIVO.COMPANIA, 'NUMERO DE DIGITOS EJE', 67, SYSDATE,-1)) = DESCRIPCION_INICIATIVA.ID
			AND BP_PLAN_INDICATIVO.VIGENCIA_INICIAL = DESCRIPCION_INICIATIVA.VIGENCIA_INICIAL
		INNER JOIN BP_PLAN_INDICATIVO DESCRIPCION_META_PRODUCTO
              ON
			BP_PLAN_INDICATIVO.COMPANIA = DESCRIPCION_META_PRODUCTO.COMPANIA
			AND SUBSTR(BP_PLAN_INDICATIVO.ID, 0, PCK_SYSMAN_UTL.FC_PAR(BP_PLAN_INDICATIVO.COMPANIA, 'NUMERO DE DIGITOS META-PRODUCTO', 67, SYSDATE,-1)) = DESCRIPCION_META_PRODUCTO.ID
			AND BP_PLAN_INDICATIVO.VIGENCIA_INICIAL = DESCRIPCION_META_PRODUCTO.VIGENCIA_INICIAL
		INNER JOIN BP_PLAN_INDICATIVO DESCRIPCION_PROGRAMA
              ON
			BP_PLAN_INDICATIVO.COMPANIA = DESCRIPCION_PROGRAMA.COMPANIA
			AND SUBSTR(BP_PLAN_INDICATIVO.ID, 0, PCK_SYSMAN_UTL.FC_PAR(BP_PLAN_INDICATIVO.COMPANIA, 'NUMERO DE DIGITOS PROGRAMA', 67, SYSDATE,-1)) = DESCRIPCION_PROGRAMA.ID
			AND BP_PLAN_INDICATIVO.VIGENCIA_INICIAL = DESCRIPCION_PROGRAMA.VIGENCIA_INICIAL
		LEFT JOIN BP_NIVEL_PLAN_IND 
              ON
			BP_PLAN_INDICATIVO.COMPANIA = BP_NIVEL_PLAN_IND.COMPANIA
			AND BP_PLAN_INDICATIVO.VIGENCIA_INICIAL = BP_NIVEL_PLAN_IND.VIGENCIA
			AND LENGTH(BP_PLAN_INDICATIVO.ID) = BP_NIVEL_PLAN_IND.DIGITOS
		LEFT JOIN BP_PLAN_INDICATIVO_METAS
ON
			BP_PLAN_INDICATIVO.COMPANIA = BP_PLAN_INDICATIVO_METAS.COMPANIA
			AND BP_PLAN_INDICATIVO.VIGENCIA_INICIAL = BP_PLAN_INDICATIVO_METAS.VIGENCIA_PLAN
			AND BP_PLAN_INDICATIVO.ID = BP_PLAN_INDICATIVO_METAS.ID_PLAN
		WHERE
			BP_PLAN_INDICATIVO.COMPANIA = s$compania$s
			AND BP_NIVEL_PLAN_IND.VIGENCIA = TO_NUMBER(PCK_SYSMAN_UTL.FC_PAR(BP_NIVEL_PLAN_IND.COMPANIA, 'VIGENCIA GUBERNAMENTAL ACTUAL', 67, SYSDATE,-1))
			AND BP_NIVEL_PLAN_IND.META_PRODUC = -1
			AND BP_PLAN_INDICATIVO_METAS.VIGENCIA_META BETWEEN
			CASE
				WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR != 'MI' THEN s$anio$s
				ELSE TO_NUMBER(PCK_SYSMAN_UTL.FC_PAR(BP_NIVEL_PLAN_IND.COMPANIA, 'VIGENCIA GUBERNAMENTAL ACTUAL', 67, SYSDATE,-1))
			END 
		AND s$anio$s
			AND PI_INDICADOR_META.VIGENCIA_META BETWEEN
			CASE
				WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR != 'MI' THEN s$anio$s
				ELSE TO_NUMBER(PCK_SYSMAN_UTL.FC_PAR(BP_NIVEL_PLAN_IND.COMPANIA, 'VIGENCIA GUBERNAMENTAL ACTUAL', 67, SYSDATE,-1))
			END 
		AND s$anio$s
		GROUP BY
			BP_PLAN_INDICATIVO.VIGENCIA_INICIAL,
			BP_PLAN_INDICATIVO.ID,
			DESCRIPCION_INICIATIVA.DESCRIPCION,
			SECTORES.DESCRIPCION,
			DESCRIPCION_PROGRAMA.DESCRIPCION,
			DESCRIPCION_META_PRODUCTO.DESCRIPCION,
			DEPENDENCIA.NOMBRE,
			BP_PLAN_INDICATIVO.DESCRIPCION,
			BP_PLAN_INDICATIVO.META,
			(CASE
				WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR = 'MI' THEN 'Incremento Acumulado'
				ELSE
				(CASE
					WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR = 'MM' THEN 'Mantenimiento'
					ELSE
					(CASE
						WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR = 'MR' THEN 'Reducción'
						ELSE
						(CASE
							WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR = 'MG' THEN 'Gestion'
							ELSE
							(CASE
								WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR = 'MIF' THEN 'Incremento Flujo'
								ELSE
								(CASE
									WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR = 'MIC' THEN 'Incremento Capacidad'
									ELSE
									(CASE
										WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR = 'MRA' THEN 'Reducción Anual'
										ELSE ' '
									END)
								END)
							END)
						END)
					END)
				END)
			END) ,
			PI_INDICADOR_META.META,
			PI_INDICADOR_META.AVANCE,
			BP_PLAN_INDICATIVO_METAS.PRESUPUESTO,
			BP_PLAN_INDICATIVO_METAS.COMPROMETIDO,
			BP_PLAN_INDICATIVO_METAS.PAGADO,
			BP_PLAN_INDICATIVO.TIPO_META_INDICADOR,
			PI_INDICADOR.LB
) CO
GROUP BY CO.VIGENCIA_INICIAL,
		CO.ID,
		CO.INICIATIVA_ESTRATEGICA,
		CO.SECTOR_INVERSION,
		CO.PROGRAMA,
		CO.META_PRODUCTO,
		CO.DEPENDENCIA,
		CO.DESCRIPCION,
		CO.META,
		CO.MET_IND,
		CO.TIPO_META_INDICADOR,
		CO.LINEA_BASE
) C