SELECT TO_CHAR(METAS.ID_PLAN) ID,
  s$por$s||'%-'|| NVL(PLAN.DESCRIPCION,'SIN NOMBRE') NOMBRES 
FROM PI_PLAN_INDICATIVO PLAN
INNER JOIN PI_PLAN_INDICATIVO_METAS METAS
  ON  PLAN.COMPANIA         = METAS.COMPANIA
  AND PLAN.TIPO             = METAS.TIPO
  AND PLAN.NUMERO           = METAS.NUMERO
  AND PLAN.ID_PLAN          = METAS.ID_PLAN
  AND PLAN.VIGENCIA_INICIAL = METAS.VIGENCIA_PLAN
WHERE METAS.COMPANIA       = s$compania$s
  AND METAS.VIGENCIA_PLAN = s$vigencia$s
  AND METAS.TIPO             = 's$tipo$s'
  AND METAS.NUMERO           = s$numero$s
GROUP  BY METAS.ID_PLAN, PLAN.DESCRIPCION
s$dependencia$s