SELECT 
BP_PLAN_ADQUISICIONES.CODIGO_UNSPSC "Código UNSPSC",
  BP_PLAN_ADQUISICIONES.DESCRIPCION "Descripción",
  BP_PLAN_ADQUISICIONES.F_ESTUDIO_PLAN "Fecha Estudio Plan",
  BP_PLAN_ADQUISICIONES.FECHA_INI "Fecha Inicio",
  BP_PLAN_ADQUISICIONES.FECHA_FIN "Fecha Fin",
  TIPOADJUDICACION.DESCRIPCION "Nombre Modalidad",
  PCK_PLAN_DESARROLLO.FC_LISTAFUENTESIDPLAN(UN_COMPANIA=>BP_PLAN_ADQUISICIONES.COMPANIA,UN_VIGENCIA=>BP_PLAN_ADQUISICIONES.VIGENCIA_INICIAL,UN_IDPLAN=>BP_PLAN_ADQUISICIONES.ID_PLAN) "Fuente",
  BP_PLAN_ADQUISICIONES.VALOR_ESTIMADO "Valor Estimado",
  BP_PLAN_ADQUISICIONES.VALOR_ESTIMADO_VIG "Valor Estimado Vigente",
  CASE
    WHEN VIGENCIAS_FUTURAS IN(0)
    THEN 'NO'
    ELSE 'SI'
  END "Vigencias Futuras",
  CASE
    WHEN ESTADO_SOLICITUD IN('A')
    THEN 'APROBADO'
    WHEN ESTADO_SOLICITUD IN('R')
    THEN 'RECHAZADO'
    ELSE 'EN ESPERA'
  END "Estado de Solicitud",
  TERCERO.NOMBRE "Nombre Responsable",
  DEPENDENCIA.NOMBRE "Nombre Dependencia"
FROM BP_PLAN_ADQUISICIONES
INNER JOIN BP_PLAN_INDICATIVO
ON BP_PLAN_ADQUISICIONES.COMPANIA          = BP_PLAN_INDICATIVO.COMPANIA
AND BP_PLAN_ADQUISICIONES.VIGENCIA_INICIAL = BP_PLAN_INDICATIVO.VIGENCIA_INICIAL
AND BP_PLAN_ADQUISICIONES.ID_PLAN          = BP_PLAN_INDICATIVO.ID
INNER JOIN BP_PLAN_ADQUISICIONES_VIGENCIA
ON BP_PLAN_ADQUISICIONES.COMPANIA          = BP_PLAN_ADQUISICIONES_VIGENCIA.COMPANIA
AND BP_PLAN_ADQUISICIONES.VIGENCIA_INICIAL = BP_PLAN_ADQUISICIONES_VIGENCIA.VIGENCIA_INICIAL
AND BP_PLAN_ADQUISICIONES.ID_PLAN          = BP_PLAN_ADQUISICIONES_VIGENCIA.ID_PLAN
AND BP_PLAN_ADQUISICIONES.CODIGO           = BP_PLAN_ADQUISICIONES_VIGENCIA.CODIGO
LEFT JOIN DEPENDENCIA
ON BP_PLAN_INDICATIVO.COMPANIA     = DEPENDENCIA.COMPANIA
AND BP_PLAN_INDICATIVO.DEPENDENCIA = DEPENDENCIA.CODIGO
LEFT JOIN TERCERO
ON BP_PLAN_INDICATIVO.COMPANIA     = TERCERO.COMPANIA
AND BP_PLAN_INDICATIVO.RESPONSABLE = TERCERO.NIT
AND BP_PLAN_INDICATIVO.SUCURSAL    = TERCERO.SUCURSAL
LEFT JOIN TIPOADJUDICACION
ON BP_PLAN_ADQUISICIONES.COMPANIA           = TIPOADJUDICACION.COMPANIA
AND BP_PLAN_ADQUISICIONES.MODALIDAD         = TIPOADJUDICACION.CODIGO
WHERE BP_PLAN_ADQUISICIONES.COMPANIA        = s$compania$s
AND BP_PLAN_ADQUISICIONES_VIGENCIA.VIGENCIA = s$vigencia$s
