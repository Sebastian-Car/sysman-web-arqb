WITH VASIG AS (SELECT SUM(NVL(VLRASIGNADO,0)) VALORASIGNADODEP 
FROM (SELECT PC.COMPANIA,  
        PC.CODIGO,  
        PC.ANO,  
        PC.VLRASIGNADO   
    FROM PLAN_DE_COMPRAS PC INNER JOIN (SELECT  DISTINCT DETALLE_PLAN_COMPRAS.COMPANIA, 
        DETALLE_PLAN_COMPRAS.ANO, 
        DETALLE_PLAN_COMPRAS.RUBRO,  
        DETALLE_PLAN_COMPRAS.DEPENDENCIA  
        FROM DETALLE_PLAN_COMPRAS  
        WHERE DETALLE_PLAN_COMPRAS.COMPANIA = s$compania$s 
            AND DETALLE_PLAN_COMPRAS.ANO = s$ano$s 
            AND DETALLE_PLAN_COMPRAS.DEPENDENCIA = 's$depInicial$s' 
            AND DETALLE_PLAN_COMPRAS.SUBPROYECTO = 's$subproyecto$s' 
            AND DETALLE_PLAN_COMPRAS.MES  = s$mesInicial$s) DPC 
    ON PC.COMPANIA = DPC.COMPANIA 
        AND PC.ANO = DPC.ANO 
        AND PC.CODIGO = DPC.RUBRO)) 
SELECT PLAN_DE_COMPRAS.COMPANIA,  
    PLAN_DE_COMPRAS.CODIGO CODIGOPC,  
    PLAN_DE_COMPRAS.ANO,  
    (DETALLE_PLAN_COMPRAS.DEPENDENCIA || DETALLE_PLAN_COMPRAS.SUBPROYECTO) DEPEYSUBP,  
    DETALLE_PLAN_COMPRAS.DEPENDENCIA,  
    DETALLE_PLAN_COMPRAS.SUBPROYECTO,  
    DETALLE_PLAN_COMPRAS.MES,  
    SUBPROYECTO.NOMBRE NOMBRESUB,  
    PLAN_DE_COMPRAS.NOMBRE NOMBREPC,  
    DEPENDENCIA.NOMBRE NOMBREDEP,  
    SUM(DETALLE_PLAN_COMPRAS.CANTIDAD) SUMADECANTIDAD,  
    SUM(DETALLE_PLAN_COMPRAS.VALOR_UNITARIO) SUMADEVALOR_UNITARIO,  
    SUM(DETALLE_PLAN_COMPRAS.VALORACOMPRAR) SUMADEVALORACOMPRAR,  
    SUM(DETALLE_PLAN_COMPRAS.COMPRADO) SUMADECOMPRADO,  
    SUM(DETALLE_PLAN_COMPRAS.VALORTOTALCOMPRADO) SUMADEVALORTOTALCOMPRADO,  
    SUM(DETALLE_PLAN_COMPRAS.ENTRADAS) SUMADEENTRADAS,  
    SUM(DETALLE_PLAN_COMPRAS.VALOR_ENTRADAS) SUMADEVALOR_ENTRADAS,  
    SUM(DETALLE_PLAN_COMPRAS.SALIDAS) SUMADESALIDAS,  
    SUM(DETALLE_PLAN_COMPRAS.VALOR_SALIDAS) SUMADEVALOR_SALIDAS,  
    INVENTARIO.NOMBRELARGO,  
    MIN(PLAN_DE_COMPRAS.VLRASIGNADO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) PRIMERODEVLRASIGNADO,  
    SUM(PLAN_DE_COMPRAS.VLRPROGRAMADO) SUMADEVLRPROGRAMADO,  
    SUM(PLAN_DE_COMPRAS.VLREJECUTADO) SUMADEVLREJECUTADO,  
    DETALLE_PLAN_COMPRAS.CODIGO CODIGODPC,  
    SUM(DETALLE_PLAN_COMPRAS.CANTIDAD)+SUM(DETALLE_PLAN_COMPRAS.ENTRADAS)-SUM(DETALLE_PLAN_COMPRAS.SALIDAS) CANTIDAD1,  
    SUM(DETALLE_PLAN_COMPRAS.VALORACOMPRAR)+SUM(DETALLE_PLAN_COMPRAS.VALOR_ENTRADAS)-SUM(DETALLE_PLAN_COMPRAS.VALOR_SALIDAS) VALORACOMPRAR1,  
    INVENTARIO.CUBS,  
    VASIG.VALORASIGNADODEP  
FROM (PLAN_DE_COMPRAS INNER JOIN ( 
        (DETALLE_PLAN_COMPRAS INNER JOIN INVENTARIO ON  
                DETALLE_PLAN_COMPRAS.COMPANIA = INVENTARIO.COMPANIA  
                AND DETALLE_PLAN_COMPRAS.CODIGO = INVENTARIO.CODIGOELEMENTO) INNER JOIN DEPENDENCIA ON  
            DETALLE_PLAN_COMPRAS.COMPANIA = DEPENDENCIA.COMPANIA  
            AND DETALLE_PLAN_COMPRAS.DEPENDENCIA = DEPENDENCIA.CODIGO) ON  
        PLAN_DE_COMPRAS.COMPANIA = DETALLE_PLAN_COMPRAS.COMPANIA  
        AND PLAN_DE_COMPRAS.CODIGO = DETALLE_PLAN_COMPRAS.RUBRO  
        AND PLAN_DE_COMPRAS.ANO = DETALLE_PLAN_COMPRAS.ANO ) INNER JOIN SUBPROYECTO ON  
    DETALLE_PLAN_COMPRAS.COMPANIA = SUBPROYECTO.COMPANIA  
    AND DETALLE_PLAN_COMPRAS.SUBPROYECTO = SUBPROYECTO.CODIGOSUBPROYECTO, VASIG 
WHERE PLAN_DE_COMPRAS.COMPANIA = s$compania$s 
    AND PLAN_DE_COMPRAS.ANO = s$ano$s  
    AND DETALLE_PLAN_COMPRAS.DEPENDENCIA = 's$depInicial$s'  
    AND DETALLE_PLAN_COMPRAS.SUBPROYECTO = 's$subproyecto$s'  
    AND DETALLE_PLAN_COMPRAS.MES = s$mesInicial$s 
GROUP BY PLAN_DE_COMPRAS.COMPANIA,  
    PLAN_DE_COMPRAS.CODIGO,  
    PLAN_DE_COMPRAS.ANO,  
    (DETALLE_PLAN_COMPRAS.DEPENDENCIA || DETALLE_PLAN_COMPRAS.SUBPROYECTO),  
    DETALLE_PLAN_COMPRAS.DEPENDENCIA,  
    DETALLE_PLAN_COMPRAS.SUBPROYECTO,  
    DETALLE_PLAN_COMPRAS.MES,  
    SUBPROYECTO.NOMBRE,  
    PLAN_DE_COMPRAS.NOMBRE,  
    INVENTARIO.NOMBRELARGO,  
    DETALLE_PLAN_COMPRAS.CODIGO,  
    INVENTARIO.CUBS,  
    DEPENDENCIA.NOMBRE, 
    VASIG.VALORASIGNADODEP
