SELECT  
    DETALLE_PLAN_COMPRAS.CODIGO DETALLE_PLAN_COMPRAS_CODIGO,  
    INVENTARIO.NOMBRELARGO,  
    SUM(DETALLE_PLAN_COMPRAS.COMPRADO)  SCOMPRADO,  
    SUM(DETALLE_PLAN_COMPRAS.VALORTOTALCOMPRADO)  SVALORTOTALCOMPRADO,  
    (SUM(DETALLE_PLAN_COMPRAS.CANTIDAD)+ SUM(DETALLE_PLAN_COMPRAS.ENTRADAS)-SUM(DETALLE_PLAN_COMPRAS.SALIDAS)) CANTIDAD1, 
    (SUM(DETALLE_PLAN_COMPRAS.VALORACOMPRAR) + SUM(DETALLE_PLAN_COMPRAS.VALOR_ENTRADAS) - SUM(DETALLE_PLAN_COMPRAS.VALOR_SALIDAS)) VALORACOMPRAR1, 
    SUM(DETALLE_PLAN_COMPRAS.VALORACOMPRAR+DETALLE_PLAN_COMPRAS.VALOR_ENTRADAS-DETALLE_PLAN_COMPRAS.VALOR_SALIDAS) VALORTOTAL, 
    PLAN_DE_COMPRAS.CODIGO PLAN_COMPRAS_CODIGO,  
    PLAN_DE_COMPRAS.NOMBRE 
FROM DETALLE_PLAN_COMPRAS  
LEFT JOIN DEPENDENCIA  
  ON DETALLE_PLAN_COMPRAS.COMPANIA = DEPENDENCIA.COMPANIA  
  AND  DETALLE_PLAN_COMPRAS.DEPENDENCIA = DEPENDENCIA.CODIGO  
LEFT JOIN INVENTARIO  
  ON DETALLE_PLAN_COMPRAS.COMPANIA = INVENTARIO.COMPANIA  
  AND DETALLE_PLAN_COMPRAS.CODIGO = INVENTARIO.CODIGOELEMENTO 
LEFT JOIN PLAN_DE_COMPRAS  
  ON DETALLE_PLAN_COMPRAS.COMPANIA = PLAN_DE_COMPRAS.COMPANIA  
  AND DETALLE_PLAN_COMPRAS.ANO = PLAN_DE_COMPRAS.ANO   
  AND DETALLE_PLAN_COMPRAS.RUBRO = PLAN_DE_COMPRAS.CODIGO  
WHERE DETALLE_PLAN_COMPRAS.COMPANIA = s$compania$s 
AND DETALLE_PLAN_COMPRAS.ANO = s$cmbano$s                
AND DETALLE_PLAN_COMPRAS.RUBRO >=   's$cmbElementoDesde$s'                
AND DETALLE_PLAN_COMPRAS.RUBRO <=  's$cmbElementoHasta$s'             
GROUP BY  
    DETALLE_PLAN_COMPRAS.CODIGO,  
    INVENTARIO.NOMBRELARGO,  
    PLAN_DE_COMPRAS.CODIGO,  
    PLAN_DE_COMPRAS.NOMBRE  
ORDER BY  
    DETALLE_PLAN_COMPRAS.CODIGO
