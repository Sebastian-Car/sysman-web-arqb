SELECT 
    INVENTARIO.CODIGOELEMENTO,
    INVENTARIO.NOMBRELARGO, 
    INVENTARIO.UNIDAD,
    MAX(D_ORDENDECOMPRA.VALORUNITARIO)  KEEP (DENSE_RANK LAST ORDER BY ROWNUM)  ÚLTIMODEVALORUNITARIO,
    MAX(TERCERO.NOMBRE)  KEEP (DENSE_RANK LAST ORDER BY ROWNUM) ÚLTIMODENOMBRE,  
    MAX(D_ORDENDECOMPRA.VALORUNITARIODI) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) ÚLTIMODEVALORUNITARIODI
FROM INVENTARIO 
LEFT JOIN (D_ORDENDECOMPRA 
LEFT JOIN ORDENDECOMPRA 
ON D_ORDENDECOMPRA.COMPANIA = ORDENDECOMPRA.COMPANIA
AND D_ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRA.CLASEORDEN
AND  D_ORDENDECOMPRA.ORDENDECOMPRA = ORDENDECOMPRA.NUMERO) 
LEFT JOIN TERCERO 
ON ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA
AND ORDENDECOMPRA.TERCERO = TERCERO.NIT  
AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL 
ON INVENTARIO.COMPANIA = D_ORDENDECOMPRA.COMPANIA
AND  INVENTARIO.CODIGOELEMENTO = D_ORDENDECOMPRA.ELEMENTO 
WHERE INVENTARIO.COMPANIA =  s$compania$s
AND INVENTARIO.CODIGOELEMENTO BETWEEN 's$cmbElementoDesde$s' AND 's$cmbElementoHasta$s'
AND TO_CHAR(FECHA, 'YYYY') = s$cmbAno$s            
AND INVENTARIO.TIENEMOVIMIENTO <> 0
GROUP BY 
    INVENTARIO.COMPANIA, 
    INVENTARIO.CODIGOELEMENTO, 
    INVENTARIO.NOMBRELARGO, 
    INVENTARIO.UNIDAD,    
    INVENTARIO.TIENEMOVIMIENTO 
s$orderBy$s
