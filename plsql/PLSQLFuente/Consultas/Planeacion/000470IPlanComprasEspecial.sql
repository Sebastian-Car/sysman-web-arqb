WITH I_PLANCOMPRAS AS (SELECT  PLAN_DE_COMPRAS.COMPANIA, 
        PLAN_DE_COMPRAS.CODIGO COD, 
        PLAN_DE_COMPRAS.ANO, 
        PLAN_DE_COMPRAS.NOMBRE NOMBREPC, 
        DETALLE_PLAN_COMPRAS.DEPENDENCIA, 
        DETALLE_PLAN_COMPRAS.MES, 
        DETALLE_PLAN_COMPRAS.CANTIDAD, 
        DETALLE_PLAN_COMPRAS.VALOR_UNITARIO, 
        DETALLE_PLAN_COMPRAS.VALORACOMPRAR, 
        DETALLE_PLAN_COMPRAS.COMPRADO, 
        DETALLE_PLAN_COMPRAS.VALORTOTALCOMPRADO, 
        DETALLE_PLAN_COMPRAS.ENTRADAS, 
        DETALLE_PLAN_COMPRAS.VALOR_ENTRADAS, 
        DETALLE_PLAN_COMPRAS.SALIDAS, 
        DETALLE_PLAN_COMPRAS.VALOR_SALIDAS, 
        INVENTARIO.NOMBRELARGO, 
        DEPENDENCIA.NOMBRE NOMBREDEP, 
        PLAN_DE_COMPRAS.VLRASIGNADO, 
        PLAN_DE_COMPRAS.VLRPROGRAMADO, 
        PLAN_DE_COMPRAS.VLREJECUTADO, 
        DETALLE_PLAN_COMPRAS.CODIGO, 
        INVENTARIO.CUBS, 
        (NVL(CANTIDAD,0)+NVL(ENTRADAS,0)-NVL(SALIDAS,0)) CANTIDAD1, 
        (VALORACOMPRAR+VALOR_ENTRADAS-VALOR_SALIDAS) VALORACOMPRAR1,
        SUM(NVL(DETALLE_PLAN_COMPRAS.VALORACOMPRAR,0)+NVL(DETALLE_PLAN_COMPRAS.VALOR_ENTRADAS,0)-NVL(DETALLE_PLAN_COMPRAS.VALOR_SALIDAS,0)) SUMAVALORACOMPRAR1,
        SUM(NVL(DETALLE_PLAN_COMPRAS.VALORTOTALCOMPRADO,0)) SUMATOTALCOMPRADO
FROM PLAN_DE_COMPRAS INNER JOIN (
        (DETALLE_PLAN_COMPRAS INNER JOIN INVENTARIO 
            ON (DETALLE_PLAN_COMPRAS.COMPANIA = INVENTARIO.COMPANIA) 
            AND (DETALLE_PLAN_COMPRAS.CODIGO = INVENTARIO.CODIGOELEMENTO)) INNER JOIN DEPENDENCIA 
                ON (DETALLE_PLAN_COMPRAS.DEPENDENCIA = DEPENDENCIA.CODIGO) 
                AND (DETALLE_PLAN_COMPRAS.COMPANIA = DEPENDENCIA.COMPANIA)) 
        ON (PLAN_DE_COMPRAS.COMPANIA = DETALLE_PLAN_COMPRAS.COMPANIA) 
        AND (PLAN_DE_COMPRAS.CODIGO = DETALLE_PLAN_COMPRAS.RUBRO) 
        AND (PLAN_DE_COMPRAS.ANO = DETALLE_PLAN_COMPRAS.ANO) 
WHERE PLAN_DE_COMPRAS.COMPANIA = s$compania$s
    AND PLAN_DE_COMPRAS.CODIGO='s$codigo$s' 
    AND PLAN_DE_COMPRAS.ANO=s$ano$s
GROUP BY PLAN_DE_COMPRAS.COMPANIA, 
        PLAN_DE_COMPRAS.CODIGO, 
        PLAN_DE_COMPRAS.ANO, 
        PLAN_DE_COMPRAS.NOMBRE, 
        DETALLE_PLAN_COMPRAS.DEPENDENCIA, 
        DETALLE_PLAN_COMPRAS.MES, 
        DETALLE_PLAN_COMPRAS.CANTIDAD, 
        DETALLE_PLAN_COMPRAS.VALOR_UNITARIO, 
        DETALLE_PLAN_COMPRAS.VALORACOMPRAR, 
        DETALLE_PLAN_COMPRAS.COMPRADO, 
        DETALLE_PLAN_COMPRAS.VALORTOTALCOMPRADO, 
        DETALLE_PLAN_COMPRAS.ENTRADAS, 
        DETALLE_PLAN_COMPRAS.VALOR_ENTRADAS, 
        DETALLE_PLAN_COMPRAS.SALIDAS, 
        DETALLE_PLAN_COMPRAS.VALOR_SALIDAS, 
        INVENTARIO.NOMBRELARGO, 
        DEPENDENCIA.NOMBRE, 
        PLAN_DE_COMPRAS.VLRASIGNADO, 
        PLAN_DE_COMPRAS.VLRPROGRAMADO, 
        PLAN_DE_COMPRAS.VLREJECUTADO, 
        DETALLE_PLAN_COMPRAS.CODIGO, 
        INVENTARIO.CUBS),
I_PLANCOMPRASESPECIALCR AS (SELECT 
    PLAN_DE_COMPRAS.COMPANIA, 
    PLAN_DE_COMPRAS.CODIGO COD, 
    PLAN_DE_COMPRAS.ANO, 
    PLAN_DE_COMPRAS.NOMBRE, 
    D_ORDENDECOMPRA.CANTIDAD, 
    D_ORDENDECOMPRA.VALORUNITARIODI, 
    D_ORDENDECOMPRA.VLRTOTAL, 
    INVENTARIO.NOMBRELARGO, 
    MIN(VLRASIGNADO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) PRIMERODEVLRASIGNADO, 
    MIN(VLRPROGRAMADO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) PRIMERODEVLRPROGRAMADO, 
    SUM(PLAN_DE_COMPRAS.VLREJECUTADO) SUMADEVLREJECUTADO,
    DETALLE_PLAN_COMPRAS.CODIGO, 
    INVENTARIO.CUBS, 
    D_ORDENDECOMPRA.CLASEORDEN, 
    D_ORDENDECOMPRA.ORDENDECOMPRA, 
    D_ORDENDECOMPRA.CLASEORDEN || ' ' || D_ORDENDECOMPRA.ORDENDECOMPRA  CONTRATO, 
    ORDENDECOMPRA.FECHA, 
    EXTRACT(YEAR FROM ORDENDECOMPRA.FECHA)  ANOORDEN
FROM 
    (
        (PLAN_DE_COMPRAS INNER JOIN (
            DETALLE_PLAN_COMPRAS INNER JOIN INVENTARIO 
                ON DETALLE_PLAN_COMPRAS.COMPANIA = INVENTARIO.COMPANIA 
                    AND DETALLE_PLAN_COMPRAS.CODIGO = INVENTARIO.CODIGOELEMENTO ) 
            ON PLAN_DE_COMPRAS.COMPANIA = DETALLE_PLAN_COMPRAS.COMPANIA 
                AND PLAN_DE_COMPRAS.CODIGO = DETALLE_PLAN_COMPRAS.RUBRO 
                AND PLAN_DE_COMPRAS.ANO = DETALLE_PLAN_COMPRAS.ANO ) INNER JOIN D_ORDENDECOMPRA 
        ON DETALLE_PLAN_COMPRAS.COMPANIA = D_ORDENDECOMPRA.COMPANIA 
            AND DETALLE_PLAN_COMPRAS.CODIGO = D_ORDENDECOMPRA.ELEMENTO 
            AND DETALLE_PLAN_COMPRAS.RUBRO = D_ORDENDECOMPRA.RUBROPPTO) INNER JOIN ORDENDECOMPRA 
    ON D_ORDENDECOMPRA.COMPANIA = ORDENDECOMPRA.COMPANIA 
        AND D_ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRA.CLASEORDEN 
        AND D_ORDENDECOMPRA.ORDENDECOMPRA = ORDENDECOMPRA.NUMERO
WHERE PLAN_DE_COMPRAS.COMPANIA = s$compania$s
    AND PLAN_DE_COMPRAS.ANO=s$ano$s 
    AND EXTRACT(YEAR FROM ORDENDECOMPRA.FECHA) = s$ano$s 
GROUP BY 
    PLAN_DE_COMPRAS.COMPANIA, 
    PLAN_DE_COMPRAS.CODIGO, 
    PLAN_DE_COMPRAS.ANO, 
    PLAN_DE_COMPRAS.NOMBRE, 
    D_ORDENDECOMPRA.CANTIDAD, 
    D_ORDENDECOMPRA.VALORUNITARIODI, 
    D_ORDENDECOMPRA.VLRTOTAL, 
    INVENTARIO.NOMBRELARGO, 
    DETALLE_PLAN_COMPRAS.CODIGO, 
    INVENTARIO.CUBS, 
    D_ORDENDECOMPRA.CLASEORDEN, 
    D_ORDENDECOMPRA.ORDENDECOMPRA, 
    D_ORDENDECOMPRA.CLASEORDEN || ' ' || D_ORDENDECOMPRA.ORDENDECOMPRA,
    ORDENDECOMPRA.FECHA, 
    EXTRACT(YEAR FROM ORDENDECOMPRA.FECHA))
SELECT I_PLANCOMPRAS.COMPANIA, 
        I_PLANCOMPRAS.COD, 
        I_PLANCOMPRAS.ANO, 
        I_PLANCOMPRAS.NOMBREPC, 
        I_PLANCOMPRAS.DEPENDENCIA, 
        I_PLANCOMPRAS.MES, 
        I_PLANCOMPRAS.CANTIDAD, 
        I_PLANCOMPRAS.VALOR_UNITARIO, 
        I_PLANCOMPRAS.VALORACOMPRAR, 
        I_PLANCOMPRAS.COMPRADO, 
        I_PLANCOMPRAS.VALORTOTALCOMPRADO, 
        I_PLANCOMPRAS.ENTRADAS, 
        I_PLANCOMPRAS.VALOR_ENTRADAS, 
        I_PLANCOMPRAS.SALIDAS, 
        I_PLANCOMPRAS.VALOR_SALIDAS, 
        I_PLANCOMPRAS.NOMBRELARGO, 
        I_PLANCOMPRAS.NOMBREDEP, 
        I_PLANCOMPRAS.VLRASIGNADO, 
        I_PLANCOMPRAS.VLRPROGRAMADO, 
        I_PLANCOMPRAS.VLREJECUTADO, 
        I_PLANCOMPRAS.CODIGO, 
        I_PLANCOMPRAS.CUBS, 
        I_PLANCOMPRAS.CANTIDAD1, 
        I_PLANCOMPRAS.VALORACOMPRAR1,
        I_PLANCOMPRAS.SUMAVALORACOMPRAR1,
        I_PLANCOMPRAS.SUMATOTALCOMPRADO, 
        NVL(VTC.SUMADEVALORTOTALCOMPRADO
,0) 
SUMADEVALORTOTALCOMPRADO
FROM I_PLANCOMPRAS LEFT JOIN (
    SELECT  SUM(NVL(I_PLANCOMPRASESPECIALCR.VLRTOTAL,0)) SUMADEVALORTOTALCOMPRADO, 
            I_PLANCOMPRASESPECIALCR.COMPANIA COMPANIAVTC, 
            I_PLANCOMPRASESPECIALCR.COD CODVTC, 
            I_PLANCOMPRASESPECIALCR.ANO ANOVTC, 
            I_PLANCOMPRASESPECIALCR.NOMBRE NOMBREVTC 
    FROM I_PLANCOMPRASESPECIALCR 
    WHERE I_PLANCOMPRASESPECIALCR.COMPANIA = s$compania$s 
        AND I_PLANCOMPRASESPECIALCR.COD = 's$codigo$s'
    GROUP BY I_PLANCOMPRASESPECIALCR.COMPANIA, I_PLANCOMPRASESPECIALCR.COD, I_PLANCOMPRASESPECIALCR.ANO, I_PLANCOMPRASESPECIALCR.NOMBRE) VTC
    ON I_PLANCOMPRAS.COMPANIA = VTC.COMPANIAVTC
    AND I_PLANCOMPRAS.COD = VTC.CODVTC
    AND I_PLANCOMPRAS.ANO = VTC.ANOVTC
