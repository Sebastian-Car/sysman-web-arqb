SELECT PLAN_DE_COMPRAS.COMPANIA,  
    PLAN_DE_COMPRAS.CODIGO CODIGOPC,  
    PLAN_DE_COMPRAS.ANO,  
    DETALLE_PLAN_COMPRAS.CANTIDAD,  
    DETALLE_PLAN_COMPRAS.VALORACOMPRAR,  
    PLAN_DE_COMPRAS.NOMBRE NOMBREPC,  
    DETALLE_PLAN_COMPRAS.DEPENDENCIA,  
    SUM(DETALLE_PLAN_COMPRAS.CANTIDAD) AS SUMADECANTIDAD,  
    SUM(DETALLE_PLAN_COMPRAS.VALOR_UNITARIO) AS SUMADEVALOR_UNITARIO,  
    SUM(DETALLE_PLAN_COMPRAS.VALORACOMPRAR) AS SUMADEVALORACOMPRAR,  
    SUM(DETALLE_PLAN_COMPRAS.COMPRADO) AS SUMADECOMPRADO,  
    SUM(DETALLE_PLAN_COMPRAS.VALORTOTALCOMPRADO) AS SUMADEVALORTOTALCOMPRADO,  
    SUM(DETALLE_PLAN_COMPRAS.ENTRADAS) AS SUMADEENTRADAS,  
    SUM(DETALLE_PLAN_COMPRAS.VALOR_ENTRADAS) AS SUMADEVALOR_ENTRADAS,  
    SUM(DETALLE_PLAN_COMPRAS.SALIDAS) AS SUMADESALIDAS,  
    SUM(DETALLE_PLAN_COMPRAS.VALOR_SALIDAS) AS SUMADEVALOR_SALIDAS,  
    INVENTARIO.NOMBRELARGO,  
    DEPENDENCIA.NOMBRE NOMBREDEP,  
    MIN(PLAN_DE_COMPRAS.VLRASIGNADO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) AS PRIMERODEVLRASIGNADO,  
    SUM(PLAN_DE_COMPRAS.VLRPROGRAMADO) AS SUMADEVLRPROGRAMADO,  
    SUM(PLAN_DE_COMPRAS.VLREJECUTADO) AS SUMADEVLREJECUTADO,  
    DETALLE_PLAN_COMPRAS.CODIGO CODIGODPC,  
    SUM(CANTIDAD)+SUM(ENTRADAS)-SUM(SALIDAS) AS CANTIDAD1, 
    SUM(VALORACOMPRAR)+SUM(VALOR_ENTRADAS)-SUM(VALOR_SALIDAS) AS VALORACOMPRAR1,  
    INVENTARIO.CUBS 
FROM  
    PLAN_DE_COMPRAS INNER JOIN ( 
        (DETALLE_PLAN_COMPRAS INNER JOIN INVENTARIO  
        ON DETALLE_PLAN_COMPRAS.CODIGO = INVENTARIO.CODIGOELEMENTO  
            AND DETALLE_PLAN_COMPRAS.COMPANIA = INVENTARIO.COMPANIA) INNER JOIN DEPENDENCIA  
    ON DETALLE_PLAN_COMPRAS.COMPANIA = DEPENDENCIA.COMPANIA  
                AND DETALLE_PLAN_COMPRAS.DEPENDENCIA = DEPENDENCIA.CODIGO)  
    ON PLAN_DE_COMPRAS.COMPANIA = DETALLE_PLAN_COMPRAS.COMPANIA  
        AND PLAN_DE_COMPRAS.CODIGO = DETALLE_PLAN_COMPRAS.RUBRO  
        AND PLAN_DE_COMPRAS.ANO = DETALLE_PLAN_COMPRAS.ANO 
        WHERE PLAN_DE_COMPRAS.COMPANIA = s$compania$s 
    AND PLAN_DE_COMPRAS.CODIGO = 's$codigo$s' 
    AND PLAN_DE_COMPRAS.ANO = s$ano$s  
    AND DETALLE_PLAN_COMPRAS.DEPENDENCIA  
        BETWEEN 's$depInicial$s' 
        AND 's$depFinal$s' 
GROUP BY  
    PLAN_DE_COMPRAS.COMPANIA,  
    PLAN_DE_COMPRAS.CODIGO,  
    PLAN_DE_COMPRAS.ANO,  
    DETALLE_PLAN_COMPRAS.CANTIDAD,  
    DETALLE_PLAN_COMPRAS.VALORACOMPRAR,  
    PLAN_DE_COMPRAS.NOMBRE,  
    DETALLE_PLAN_COMPRAS.DEPENDENCIA,  
    INVENTARIO.NOMBRELARGO,  
    DEPENDENCIA.NOMBRE,  
    DETALLE_PLAN_COMPRAS.CODIGO,  
    INVENTARIO.CUBS
