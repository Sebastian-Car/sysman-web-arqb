SELECT  COMPANIA.NOMBRE, 
        COMPANIA.DIRECCION,
        COMPANIA.TELEFONO,
        COMPANIA.PAGINAWEB,
        COMPANIA.MISION,
        COMPANIA.VISION,
        DECLARACIONESTRATEGICA.PERSPECTIVA,
        COMPANIA.CONTACTO,
        DECLARACIONESTRATEGICA.VALORPAA,
        DECLARACIONESTRATEGICA.MENOR_CUANTIA,
        DECLARACIONESTRATEGICA.MINIMA_CUANTIA,
        DECLARACIONESTRATEGICA.FECHA_ACTUALIZACION
FROM COMPANIA 
  INNER JOIN DECLARACIONESTRATEGICA 
    ON COMPANIA.CODIGO = DECLARACIONESTRATEGICA.COMPANIA
  INNER JOIN ANO
    ON DECLARACIONESTRATEGICA.COMPANIA = ANO.COMPANIA
    AND DECLARACIONESTRATEGICA.ANO = ANO.NUMERO
WHERE COMPANIA.CODIGO = s$compania$s 
  AND ANO.NUMERO = s$anio$s
