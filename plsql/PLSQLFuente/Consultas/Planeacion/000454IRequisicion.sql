SELECT DISTINCT  
    DETALLEREQUIS.COD_REQUISICION,  
    REQUISICION.FECHAREQUISICION,  
    REQUISICION.VALORESTIMADO,   
    DETALLEREQUIS.CODIGOELEMENTO,  
    DETALLEREQUIS.ESPECIFICACION,  
    DETALLEREQUIS.CANTIDAD,  
    DETALLEREQUIS.VALORUNITARIO,  
    DETALLEREQUIS.VALORTOTAL,    
    INVENTARIO.NOMBRELARGO, 
    INVENTARIO.NOMBRELARGO ||' '|| DETALLEREQUIS.ESPECIFICACION NOMBREARTICULO, 
    'LA - EL ' || RESPONSABLE.CARGO|| ' de '|| DEPENDENCIA.NOMBRE || ' solicita se autorice a quien corresponda, el suministro de los elementos que se relacionan a continuación, para emplearse en ' || REQUISICION.DESCRIPCION || '.'  TEXTODETALLE, 
    INVENTARIO.UNIDAD,  
    DEPENDENCIA.NOMBRE DEPENDENCIA_NOMBRE,  
    TERCERO.NOMBRE TERCERO_NOMBRE,  
    REQUISICION.RESPONSABLE,  
    RESPONSABLE.CARGO 
FROM DETALLEREQUIS  
  LEFT JOIN REQUISICION  
    ON  DETALLEREQUIS.COMPANIA = REQUISICION.COMPANIA  
    AND DETALLEREQUIS.COD_REQUISICION = REQUISICION.COD_REQUISICION  
  LEFT JOIN INVENTARIO  
    ON  DETALLEREQUIS.COMPANIA = INVENTARIO.COMPANIA 
    AND DETALLEREQUIS.CODIGOELEMENTO = INVENTARIO.CODIGOELEMENTO 
  LEFT JOIN RESPONSABLE  
    ON  REQUISICION.COMPANIA = RESPONSABLE.COMPANIA 
    AND REQUISICION.RESPONSABLE = RESPONSABLE.CEDULA  
    AND REQUISICION.SUCURSAL = RESPONSABLE.SUCURSAL 
  LEFT JOIN TERCERO 
    ON  RESPONSABLE.COMPANIA = TERCERO.COMPANIA 
    AND RESPONSABLE.CEDULA = TERCERO.NIT 
    AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL 
  LEFT JOIN DEPENDENCIA  
    ON  REQUISICION.COMPANIA = DEPENDENCIA.COMPANIA 
    AND REQUISICION.DEPENDENCIA = DEPENDENCIA.CODIGO 
WHERE DETALLEREQUIS.COMPANIA =  s$compania$s 
    AND DETALLEREQUIS.COD_REQUISICION =  s$codRequisicion$s  
ORDER BY  
     DETALLEREQUIS.COD_REQUISICION
