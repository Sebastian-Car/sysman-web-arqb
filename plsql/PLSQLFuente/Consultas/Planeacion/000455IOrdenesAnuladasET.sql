SELECT ORDENDECOMPRA.FECHA, 
       ORDENDECOMPRA.FECHAANULACION, 
       ORDENDECOMPRA.CLASEORDEN || '-' || ORDENDECOMPRA.NUMERO AS NUMEROORDEN, 
       ORDENDECOMPRAPPTO.NUMEROPPTO AS DISPONIBILIDAD, 
       ORDENDECOMPRAPPTO.RUBRO, 
       ORDENDECOMPRA.OBJETOCONTRATO, 
       ORDENDECOMPRA.VALORTOTAL, 
       TERCERO.NIT || '-' || TERCERO.DIGITOVERIFICACION || ' ' || TERCERO.NOMBRE AS PROPONENTE, 
       DEPENDENCIA.NOMBRE AS NOMBREDEP, 
       V_ORDENDECOMPRA_RES.NUMERO AS RESERVA     
 FROM DEPENDENCIA  
   INNER JOIN ORDENDECOMPRA  
     ON DEPENDENCIA.COMPANIA      = ORDENDECOMPRA.COMPANIA 
     AND DEPENDENCIA.CODIGO       = ORDENDECOMPRA.DEPENDENCIA  
   LEFT JOIN ORDENDECOMPRAPPTO 
     ON ORDENDECOMPRA.COMPANIA    = ORDENDECOMPRAPPTO.COMPANIA 
     AND ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRAPPTO.CLASEORDEN 
     AND ORDENDECOMPRA.NUMERO     = ORDENDECOMPRAPPTO.NUMERO   
   LEFT JOIN TERCERO  
     ON ORDENDECOMPRA.COMPANIA    = TERCERO.COMPANIA  
     AND ORDENDECOMPRA.TERCERO    = TERCERO.NIT 
     AND   ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL  
   LEFT JOIN V_ORDENDECOMPRA_RES 
     ON ORDENDECOMPRA.COMPANIA    = V_ORDENDECOMPRA_RES.COMPANIA 
     AND ORDENDECOMPRA.CLASEORDEN = V_ORDENDECOMPRA_RES.CLASEORDEN 
     AND ORDENDECOMPRA.NUMERO     = V_ORDENDECOMPRA_RES.NUMERO   
 WHERE ORDENDECOMPRA.COMPANIA     = s$compania$s 
  AND ORDENDECOMPRA.ESTADO        = 'A' 
  AND  TO_CHAR(TO_DATE(ORDENDECOMPRA.FECHA) , 'YYYYMMDD') BETWEEN  TO_CHAR(s$fechaInicial$s , 'YYYYMMDD') AND TO_CHAR(s$fechaFinal$s,'YYYYMMDD') 
ORDER BY ORDENDECOMPRA.COMPANIA,  
	 ORDENDECOMPRA.CLASEORDEN,  
	 ORDENDECOMPRA.NUMERO
