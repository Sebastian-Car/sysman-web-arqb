WITH EJECUTADO AS(SELECT DETALLE_PLAN_COMPRAS.COMPANIA, DETALLE_PLAN_COMPRAS.RUBRO,DETALLE_PLAN_COMPRAS.ANO,  SUM(D_ORDENDECOMPRA.VLRTOTAL) VLREJECUTADO
FROM  DETALLE_PLAN_COMPRAS
INNER JOIN D_ORDENDECOMPRA
ON (DETALLE_PLAN_COMPRAS.COMPANIA = D_ORDENDECOMPRA.COMPANIA)
AND (DETALLE_PLAN_COMPRAS.CODIGO  = D_ORDENDECOMPRA.ELEMENTO)
AND (DETALLE_PLAN_COMPRAS.RUBRO   = D_ORDENDECOMPRA.RUBROPPTO)
WHERE DETALLE_PLAN_COMPRAS.COMPANIA = s$compania$s 
AND DETALLE_PLAN_COMPRAS.ANO = s$ano$s
GROUP BY DETALLE_PLAN_COMPRAS.COMPANIA, DETALLE_PLAN_COMPRAS.RUBRO, DETALLE_PLAN_COMPRAS.ANO), 
DETALLE AS (
SELECT 
DETALLE_PLAN_COMPRAS.VALORTOTALCOMPRADO,
PLAN_DE_COMPRAS.COMPANIA,
  PLAN_DE_COMPRAS.CODIGO AS COD,
  PLAN_DE_COMPRAS.ANO,
  PLAN_DE_COMPRAS.NOMBRE PLANCOMPRASNOMBRE,
  DETALLE_PLAN_COMPRAS.DEPENDENCIA,
  DETALLE_PLAN_COMPRAS.MES,
  DETALLE_PLAN_COMPRAS.CANTIDAD,
  DETALLE_PLAN_COMPRAS.VALOR_UNITARIO,
  DETALLE_PLAN_COMPRAS.VALORACOMPRAR,
  DETALLE_PLAN_COMPRAS.COMPRADO,
  DETALLE_PLAN_COMPRAS.ENTRADAS,
  DETALLE_PLAN_COMPRAS.VALOR_ENTRADAS,
  DETALLE_PLAN_COMPRAS.SALIDAS,
  DETALLE_PLAN_COMPRAS.VALOR_SALIDAS,
  INVENTARIO.NOMBRELARGO,
  DEPENDENCIA.NOMBRE NOMBREDEP,
  PLAN_DE_COMPRAS.VLRASIGNADO,
  PLAN_DE_COMPRAS.VLRPROGRAMADO,
  DETALLE_PLAN_COMPRAS.CODIGO,
  INVENTARIO.CUBS,
  CANTIDAD     +ENTRADAS-SALIDAS             AS CANTIDAD1,
  VALORACOMPRAR+VALOR_ENTRADAS-VALOR_SALIDAS AS VALORACOMPRAR1, NVL(EJECUTADO.VLREJECUTADO,0)VLREJECUTADO
FROM (PLAN_DE_COMPRAS
INNER JOIN ((DETALLE_PLAN_COMPRAS
INNER JOIN INVENTARIO
ON (DETALLE_PLAN_COMPRAS.COMPANIA = INVENTARIO.COMPANIA)
AND (DETALLE_PLAN_COMPRAS.CODIGO  = INVENTARIO.CODIGOELEMENTO))
INNER JOIN DEPENDENCIA
ON (DETALLE_PLAN_COMPRAS.DEPENDENCIA = DEPENDENCIA.CODIGO)
AND (DETALLE_PLAN_COMPRAS.COMPANIA   = DEPENDENCIA.COMPANIA))
ON (PLAN_DE_COMPRAS.COMPANIA         = DETALLE_PLAN_COMPRAS.COMPANIA)
AND (PLAN_DE_COMPRAS.CODIGO          = DETALLE_PLAN_COMPRAS.RUBRO)
AND (PLAN_DE_COMPRAS.ANO             = DETALLE_PLAN_COMPRAS.ANO))
LEFT JOIN EJECUTADO 
ON (PLAN_DE_COMPRAS.COMPANIA = EJECUTADO.COMPANIA
  AND PLAN_DE_COMPRAS.CODIGO = EJECUTADO.RUBRO
  AND PLAN_DE_COMPRAS.ANO = EJECUTADO.ANO)
  WHERE PLAN_DE_COMPRAS.COMPANIA = s$compania$s
  AND PLAN_DE_COMPRAS.ANO = s$ano$s
GROUP BY PLAN_DE_COMPRAS.COMPANIA,
  PLAN_DE_COMPRAS.CODIGO,
  PLAN_DE_COMPRAS.ANO,
  PLAN_DE_COMPRAS.NOMBRE,
  DETALLE_PLAN_COMPRAS.DEPENDENCIA,
  DETALLE_PLAN_COMPRAS.MES,
  DETALLE_PLAN_COMPRAS.CANTIDAD,
  DETALLE_PLAN_COMPRAS.VALOR_UNITARIO,
  DETALLE_PLAN_COMPRAS.VALORACOMPRAR,
  DETALLE_PLAN_COMPRAS.COMPRADO,
  DETALLE_PLAN_COMPRAS.VALORTOTALCOMPRADO,
  DETALLE_PLAN_COMPRAS.ENTRADAS,
  DETALLE_PLAN_COMPRAS.VALOR_ENTRADAS,
  DETALLE_PLAN_COMPRAS.SALIDAS,
  DETALLE_PLAN_COMPRAS.VALOR_SALIDAS,
  INVENTARIO.NOMBRELARGO,
  DEPENDENCIA.NOMBRE ,
  PLAN_DE_COMPRAS.VLRASIGNADO,
  PLAN_DE_COMPRAS.VLRPROGRAMADO,
  PLAN_DE_COMPRAS.VLREJECUTADO,
  DETALLE_PLAN_COMPRAS.CODIGO,
  INVENTARIO.CUBS,
  EJECUTADO.VLREJECUTADO),
  DETDEPEDENCIA AS(
  SELECT DETALLE.COD,DETALLE.DEPENDENCIA, SUM(VALORTOTALCOMPRADO) EJECTDEP,SUM(VALORACOMPRAR+VALOR_ENTRADAS-VALOR_SALIDAS)ASIGDEP FROM DETALLE GROUP BY DETALLE.DEPENDENCIA, DETALLE.COD)
 
  SELECT DETALLE.*,DETDEPEDENCIA.EJECTDEP,DETDEPEDENCIA.ASIGDEP 
  FROM DETALLE INNER JOIN DETDEPEDENCIA 
  ON (DETALLE.COD=DETDEPEDENCIA.COD AND DETALLE.DEPENDENCIA=DETDEPEDENCIA.DEPENDENCIA)
