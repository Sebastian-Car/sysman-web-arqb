SELECT  
        ORDENDECOMPRA.FECHAANULACION AS FECHAINFORME,
        D_ORDENDECOMPRA.ORDENDECOMPRA, 
        TERCERO.NOMBRE AS NOMBRETERCERO, 
        CASE WHEN D_ORDENDECOMPRA.ELEMENTO <> 9999999999999999
          THEN INVENTARIO.NOMBRELARGO || ' ' || D_ORDENDECOMPRA.ESPECIFICACION
          ELSE D_ORDENDECOMPRA.ESPECIFICACION
        END AS ELEMENTO, 
        DEPENDENCIA.NOMBRE, 
        D_ORDENDECOMPRA.VLRTOTAL,
        D_ORDENDECOMPRA.RUBROPPTO, 
        CASE WHEN ORDENDECOMPRA.ESTADO='A'
          THEN TIPOORDENDECOMPRA.NOMBRE || ' ' || 'ANULADA'
          ELSE TIPOORDENDECOMPRA.NOMBRE
        END AS NOMBREORDEN 
FROM D_ORDENDECOMPRA 
  INNER JOIN ORDENDECOMPRA 
    ON D_ORDENDECOMPRA.COMPANIA = ORDENDECOMPRA.COMPANIA 
    AND D_ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRA.CLASEORDEN 
    AND D_ORDENDECOMPRA.ORDENDECOMPRA = ORDENDECOMPRA.NUMERO 
  LEFT JOIN TERCERO 
    ON ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA 
    AND ORDENDECOMPRA.TERCERO = TERCERO.NIT 
    AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL 
  LEFT JOIN DEPENDENCIA 
    ON D_ORDENDECOMPRA.DEPENDENCIA = DEPENDENCIA.CODIGO 
    AND D_ORDENDECOMPRA.COMPANIA = DEPENDENCIA.COMPANIA 
  LEFT JOIN TIPOORDENDECOMPRA 
    ON D_ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA 
    AND D_ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO 
  LEFT JOIN COMPANIA 
    ON D_ORDENDECOMPRA.COMPANIA = COMPANIA.CODIGO 
  LEFT JOIN INVENTARIO 
    ON D_ORDENDECOMPRA.ELEMENTO = INVENTARIO.CODIGOELEMENTO 
    AND D_ORDENDECOMPRA.COMPANIA = INVENTARIO.COMPANIA
WHERE ORDENDECOMPRA.COMPANIA = s$compania$s
 
  AND ORDENDECOMPRA.FECHAANULACION BETWEEN s$fechaInicial$s AND s$fechaFinal$s 
  AND D_ORDENDECOMPRA.RUBROPPTO BETWEEN s$rubroInicial$s AND s$rubroFinal$s
   s$generaContrato$s

 AND ORDENDECOMPRA.ESTADO ='A'
ORDER BY  D_ORDENDECOMPRA.COMPANIA, 
          D_ORDENDECOMPRA.CLASEORDEN, 
          D_ORDENDECOMPRA.ORDENDECOMPRA, 
          D_ORDENDECOMPRA.CODIGO, 
          ORDENDECOMPRA.ANULADA
