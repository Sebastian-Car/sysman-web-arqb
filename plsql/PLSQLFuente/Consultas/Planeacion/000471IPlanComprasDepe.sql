WITH DETALLE AS ( SELECT PLAN_DE_COMPRAS.COMPANIA,
  PLAN_DE_COMPRAS.CODIGO CODIGOPC,
  PLAN_DE_COMPRAS.ANO,
  DETALLE_PLAN_COMPRAS.DEPENDENCIA,
    PLAN_DE_COMPRAS.VLRASIGNADO,
  DETALLE_PLAN_COMPRAS.VALORTOTALCOMPRADO
FROM PLAN_DE_COMPRAS
INNER JOIN DETALLE_PLAN_COMPRAS
ON PLAN_DE_COMPRAS.ANO              = DETALLE_PLAN_COMPRAS.ANO
AND PLAN_DE_COMPRAS.CODIGO          = DETALLE_PLAN_COMPRAS.RUBRO
AND PLAN_DE_COMPRAS.COMPANIA        = DETALLE_PLAN_COMPRAS.COMPANIA
WHERE PLAN_DE_COMPRAS.COMPANIA       ='001'
AND PLAN_DE_COMPRAS.ANO              = 2017),
SUMATORIA AS (
SELECT SUM(VLRASIGNADO)VLRASIGNADOTOT, SUM(VALORTOTALCOMPRADO)VLREJECUTADOTOT,DEPENDENCIA,COMPANIA,ANO
FROM DETALLE 
GROUP BY DEPENDENCIA,COMPANIA,ANO)
 SELECT
 CASE WHEN SUMATORIA.VLRASIGNADOTOT IS NULL  THEN PLAN_DE_COMPRAS.VLRASIGNADO ELSE SUMATORIA.VLRASIGNADOTOT END VLRASIGNADOTOT,
 CASE WHEN SUMATORIA.VLREJECUTADOTOT IS NULL THEN DETALLE_PLAN_COMPRAS.VALORTOTALCOMPRADO ELSE SUMATORIA.VLREJECUTADOTOT END VLREJECUTADOTOT,
 PLAN_DE_COMPRAS.COMPANIA,
  PLAN_DE_COMPRAS.CODIGO CODIGOPC,
  PLAN_DE_COMPRAS.ANO,
  PLAN_DE_COMPRAS.NOMBRE NOMBREPC,
  DETALLE_PLAN_COMPRAS.DEPENDENCIA,
  DETALLE_PLAN_COMPRAS.MES,
  DETALLE_PLAN_COMPRAS.CANTIDAD,
  DETALLE_PLAN_COMPRAS.VALOR_UNITARIO,
  DETALLE_PLAN_COMPRAS.VALORACOMPRAR,
  DETALLE_PLAN_COMPRAS.COMPRADO,
  DETALLE_PLAN_COMPRAS.VALORTOTALCOMPRADO,
  DETALLE_PLAN_COMPRAS.ENTRADAS,
  DETALLE_PLAN_COMPRAS.VALOR_ENTRADAS,
  DETALLE_PLAN_COMPRAS.SALIDAS,
  DETALLE_PLAN_COMPRAS.VALOR_SALIDAS,
  INVENTARIO.NOMBRELARGO,
  DEPENDENCIA.NOMBRE NOMBREDEP,
  PLAN_DE_COMPRAS.VLRASIGNADO,
  PLAN_DE_COMPRAS.VLRPROGRAMADO,
  PLAN_DE_COMPRAS.VLREJECUTADO,
  DETALLE_PLAN_COMPRAS.CODIGO CODIGODPC,
  CANTIDAD     +ENTRADAS-SALIDAS             AS CANTIDAD1,
  VALORACOMPRAR+VALOR_ENTRADAS-VALOR_SALIDAS AS VALORACOMPRAR1,
  INVENTARIO.CUBS
FROM (PLAN_DE_COMPRAS
LEFT JOIN COMPANIA
ON PLAN_DE_COMPRAS.COMPANIA = COMPANIA.CODIGO)
INNER JOIN ((DETALLE_PLAN_COMPRAS
INNER JOIN INVENTARIO
ON (DETALLE_PLAN_COMPRAS.COMPANIA = INVENTARIO.COMPANIA)
AND (DETALLE_PLAN_COMPRAS.CODIGO  = INVENTARIO.CODIGOELEMENTO))
INNER JOIN DEPENDENCIA
ON (DETALLE_PLAN_COMPRAS.DEPENDENCIA   = DEPENDENCIA.CODIGO)
AND (DETALLE_PLAN_COMPRAS.COMPANIA     = DEPENDENCIA.COMPANIA))
ON (PLAN_DE_COMPRAS.ANO                = DETALLE_PLAN_COMPRAS.ANO)
AND (PLAN_DE_COMPRAS.CODIGO            = DETALLE_PLAN_COMPRAS.RUBRO)
AND (PLAN_DE_COMPRAS.COMPANIA          = DETALLE_PLAN_COMPRAS.COMPANIA)
LEFT JOIN SUMATORIA 
ON DETALLE_PLAN_COMPRAS.COMPANIA = SUMATORIA.COMPANIA
AND DETALLE_PLAN_COMPRAS.ANO     = SUMATORIA.ANO
AND DETALLE_PLAN_COMPRAS.DEPENDENCIA=SUMATORIA.DEPENDENCIA
WHERE PLAN_DE_COMPRAS.COMPANIA = s$compania$s
AND PLAN_DE_COMPRAS.ANO = s$ano$s
AND DETALLE_PLAN_COMPRAS.DEPENDENCIA ='s$depInicial$s'
AND DETALLE_PLAN_COMPRAS.MES BETWEEN s$mesInicial$s AND s$mesFinal$s
