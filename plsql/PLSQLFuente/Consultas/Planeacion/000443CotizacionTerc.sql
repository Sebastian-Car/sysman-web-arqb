SELECT 
  COMPANIA.CODIGO,
  COMPANIA.NOMBRE NOMCOMPANIA,
  COMPANIA.SIGLACOMPANIA,
  COMPANIA.TELEFONO,
  COMPANIA.DIRECCION DIRCOMPANIA,
  REQUISICION.DESCRIPCION,
  DETALLEREQUIS.COD_DETALLE,
  DETALLEREQUIS.ESPECIFICACION,
  DETALLEREQUIS.CANTIDAD,
  TERCERO.NOMBRE NOMTERCERO,
  TERCERO.TELEFONOS,
  NVL(TERCERO.FAX,' ') FAX,
  CIUDAD.NOMBRE NOMCIUDAD,
  DEPARTAMENTO.NOMBRE NOMDEPARTAMENTO,
  PROPUESTA.NIT,
  TERCERO.DIRECCION DIRTERCERO,
  INVENTARIO.NOMBRELARGO,
  CASE WHEN INVENTARIO.NOMBRELARGO IS NOT NULL AND INVENTARIO.CODIGOELEMENTO<>'9999999999999999' THEN INVENTARIO.NOMBRELARGO||' ' ELSE '' END || CASE WHEN DETALLEREQUIS.ESPECIFICACION IS NOT NULL THEN DETALLEREQUIS.ESPECIFICACION ELSE '' END DESCRIPCION1,
  INVENTARIO.UNIDAD,
  PCK_SYSMAN_UTL.FC_PAR(s$compania$s,'PRIMER MENSAJE EN COTIZACION',s$modulo$s ,SYSDATE) PRIMER_MENSAJE_EN_COTIZACION,
  PCK_SYSMAN_UTL.FC_PAR(s$compania$s,'SEGUNDO MENSAJE EN COTIZACION',s$modulo$s ,SYSDATE) SEGUNDO_MENSAJE_EN_COTIZACION
FROM COMPANIA INNER JOIN ((DEPARTAMENTO 
  RIGHT JOIN (CIUDAD 
  RIGHT JOIN TERCERO 
    ON CIUDAD.PAIS = TERCERO.PAIS 
    AND CIUDAD.DEPARTAMENTO = TERCERO.DEPARTAMENTO
    AND CIUDAD.CODIGO = TERCERO.CIUDAD)
  ON DEPARTAMENTO.CODIGO = CIUDAD.DEPARTAMENTO 
  AND DEPARTAMENTO.PAIS = CIUDAD.PAIS) 
  INNER JOIN ((REQUISICION 
  INNER JOIN (DETALLEREQUIS 
  LEFT JOIN INVENTARIO
    ON  DETALLEREQUIS.COMPANIA = INVENTARIO.COMPANIA
    AND DETALLEREQUIS.CODIGOELEMENTO = INVENTARIO.CODIGOELEMENTO)
  ON  REQUISICION.COMPANIA = DETALLEREQUIS.COMPANIA
  AND REQUISICION.COD_REQUISICION = DETALLEREQUIS.COD_REQUISICION) 
  INNER JOIN PROPUESTA 
    ON  REQUISICION.COMPANIA = PROPUESTA.COMPANIA
    AND REQUISICION.COD_REQUISICION = PROPUESTA.COD_REQUISICION) 
  ON  TERCERO.COMPANIA = PROPUESTA.COMPANIA 
  AND TERCERO.SUCURSAL = PROPUESTA.SUCURSAL 
  AND TERCERO.NIT = PROPUESTA.NIT)
  ON COMPANIA.CODIGO = REQUISICION.COMPANIA
WHERE COMPANIA.CODIGO=s$compania$s
AND DETALLEREQUIS.COD_REQUISICION=s$codReq$s
