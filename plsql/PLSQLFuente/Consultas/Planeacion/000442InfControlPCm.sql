SELECT  DETALLE_PLAN_COMPRAS.RUBRO,  
        DETALLE_PLAN_COMPRAS.CODIGO,  
        INVENTARIO.CODIGOELEMENTO,  
        SUM(DETALLE_PLAN_COMPRAS.VALORACOMPRAR) AS SUMADEVALORACOMPRAR,  
        SUM(DETALLE_PLAN_COMPRAS.COMPRADO) AS SUMADECOMPRADO,  
        SUM((VALORACOMPRAR-COMPRADO)) AS VALORTOTAL,  
        INVENTARIO.NOMBRELARGO,  
        CASE WHEN (SUM(VALORACOMPRAR)<>0) THEN ROUND((SUM(VALORTOTALCOMPRADO*100))/SUM(VALORACOMPRAR),2) ELSE 0 END AS POREJECU,  
        (100-CASE WHEN (SUM(VALORACOMPRAR)<>0) THEN ROUND((SUM(VALORTOTALCOMPRADO*100))/SUM(VALORACOMPRAR),2) ELSE 0 END) AS PORSINEJE,  
        INVENTARIO.CODIGOCUBS, 
        SUM(DETALLE_PLAN_COMPRAS.VALORTOTALCOMPRADO) AS SUMADEVALORTOTALCOMPRADO,  
        DETALLE_PLAN_COMPRAS.ANO,  
        SUM(DETALLE_PLAN_COMPRAS.VALOR_UNITARIO) AS SUMADEVALOR_UNITARIO,  
        SUM(DETALLE_PLAN_COMPRAS.CANTIDAD) AS SUMADECANTIDAD,  
        SUM((VALOR_UNITARIO*CANTIDAD)) AS VLRTOTAL,  
        SUM(VALORACOMPRAR-VALORTOTALCOMPRADO) AS SALDO 
FROM DETALLE_PLAN_COMPRAS  
  LEFT JOIN INVENTARIO  
    ON DETALLE_PLAN_COMPRAS.COMPANIA = INVENTARIO.COMPANIA  
    AND DETALLE_PLAN_COMPRAS.CODIGO = INVENTARIO.CODIGOELEMENTO 
WHERE DETALLE_PLAN_COMPRAS.COMPANIA = s$compania$s 
  AND DETALLE_PLAN_COMPRAS.MES BETWEEN TO_CHAR(s$fechaInicial$s, 'MM') AND TO_CHAR(s$fechaFinal$s, 'MM')   
  AND DETALLE_PLAN_COMPRAS.RUBRO BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 
  AND DETALLE_PLAN_COMPRAS.ANO = TO_CHAR(s$ano$s , 'YYYY') 
GROUP BY  DETALLE_PLAN_COMPRAS.RUBRO,  
          DETALLE_PLAN_COMPRAS.CODIGO,  
          INVENTARIO.CODIGOELEMENTO, 
          INVENTARIO.NOMBRELARGO,  
          INVENTARIO.CODIGOCUBS, 
          DETALLE_PLAN_COMPRAS.ANO
ORDER BY         INVENTARIO.NOMBRELARGO
