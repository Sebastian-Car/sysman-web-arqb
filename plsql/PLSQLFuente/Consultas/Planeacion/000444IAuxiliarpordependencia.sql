SELECT ORDENDECOMPRA.NUMERO,
       D_ORDENDECOMPRA.ELEMENTO,
       D_ORDENDECOMPRA.ESPECIFICACION, 
       D_ORDENDECOMPRA.CANTIDAD,
       CASE WHEN D_ORDENDECOMPRA.CANTIDAD = 0 THEN 0 ELSE D_ORDENDECOMPRA.VLRTOTAL / CANTIDAD END VALOR_NETO,
       ORDENDECOMPRA.CLASEORDEN || ' ' || ORDENDECOMPRA.NUMERO  CLASEYNUMERO, 
       D_ORDENDECOMPRA.VALORUNITARIO,
       D_ORDENDECOMPRA.VLRTOTAL,
       ORDENDESUMINISTRO.NUMERO  REQUISICION, 
       INVENTARIO.NOMBRECORTO || ' ' || D_ORDENDECOMPRA.ESPECIFICACION  NOMBREELYESPE, 
       TO_CHAR(ORDENDECOMPRA.FECHA, 'DD/MM/YYYY')  FECHA1,     
       ORDENDECOMPRA.TERCERO,
       TERCERO.NOMBRE TERCERO_NOMBRE
FROM   D_ORDENDECOMPRA 
LEFT JOIN ORDENDESUMINISTRO 
ON D_ORDENDECOMPRA.COMPANIA = ORDENDESUMINISTRO.COMPANIA 
AND  D_ORDENDECOMPRA.ORDENDESUMINISTRO = ORDENDESUMINISTRO.NUMERO 
INNER JOIN ORDENDECOMPRA 
INNER JOIN TERCERO 
ON ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA  
AND  ORDENDECOMPRA.TERCERO = TERCERO.NIT 
AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL
ON D_ORDENDECOMPRA.COMPANIA = ORDENDECOMPRA.COMPANIA
AND D_ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRA.CLASEORDEN 
AND D_ORDENDECOMPRA.ORDENDECOMPRA = ORDENDECOMPRA.NUMERO
LEFT JOIN INVENTARIO 
ON D_ORDENDECOMPRA.COMPANIA = INVENTARIO.COMPANIA 
AND  D_ORDENDECOMPRA.ELEMENTO = INVENTARIO.CODIGOELEMENTO 
WHERE TRUNC(ORDENDECOMPRA.FECHA) BETWEEN 's$fechaDesde$s' AND 's$fechaHasta$s'
AND D_ORDENDECOMPRA.DEPENDENCIA = 's$dependencia$s'
