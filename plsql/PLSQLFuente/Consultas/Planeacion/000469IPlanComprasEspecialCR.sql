WITH EJECUTADO AS(SELECT DETALLE_PLAN_COMPRAS.COMPANIA, DETALLE_PLAN_COMPRAS.RUBRO,DETALLE_PLAN_COMPRAS.ANO,  SUM(D_ORDENDECOMPRA.VLRTOTAL) SUMADEVALORTOTALCOMPRADO
FROM  DETALLE_PLAN_COMPRAS
INNER JOIN D_ORDENDECOMPRA
ON (DETALLE_PLAN_COMPRAS.COMPANIA = D_ORDENDECOMPRA.COMPANIA)
AND (DETALLE_PLAN_COMPRAS.CODIGO  = D_ORDENDECOMPRA.ELEMENTO)
AND (DETALLE_PLAN_COMPRAS.RUBRO   = D_ORDENDECOMPRA.RUBROPPTO)
GROUP BY DETALLE_PLAN_COMPRAS.COMPANIA, DETALLE_PLAN_COMPRAS.RUBRO, DETALLE_PLAN_COMPRAS.ANO)

SELECT
EJECUTADO.SUMADEVALORTOTALCOMPRADO,
MIN(plan_de_compras.VLRASIGNADO) AS PrimeroDeVLRASIGNADO,
MIN(plan_de_compras.VLRPROGRAMADO) AS PrimeroDeVLRPROGRAMADO,
PLAN_DE_COMPRAS.COMPANIA,
  PLAN_DE_COMPRAS.CODIGO AS COD,
  PLAN_DE_COMPRAS.ANO,
  PLAN_DE_COMPRAS.NOMBRE,
  D_ORDENDECOMPRA.CANTIDAD,
  D_ORDENDECOMPRA.VALORUNITARIODI,
  D_ORDENDECOMPRA.VLRTOTAL,
  INVENTARIO.NOMBRELARGO,
  SUM(PLAN_DE_COMPRAS.VLREJECUTADO)    AS SUMADEVLREJECUTADO,
  DETALLE_PLAN_COMPRAS.CODIGO,
  INVENTARIO.CUBS,
  D_ORDENDECOMPRA.CLASEORDEN,
  D_ORDENDECOMPRA.ORDENDECOMPRA,
  D_ORDENDECOMPRA.CLASEORDEN
  ||' '|| D_ORDENDECOMPRA.ORDENDECOMPRA AS CONTRATO,
  ORDENDECOMPRA.FECHA,
  EXTRACT (YEAR FROM ORDENDECOMPRA.FECHA) AS ANOORDEN
FROM PLAN_DE_COMPRAS
INNER JOIN DETALLE_PLAN_COMPRAS
INNER JOIN INVENTARIO
ON DETALLE_PLAN_COMPRAS.CODIGO    = INVENTARIO.CODIGOELEMENTO
AND DETALLE_PLAN_COMPRAS.COMPANIA = INVENTARIO.COMPANIA
ON PLAN_DE_COMPRAS.ANO            = DETALLE_PLAN_COMPRAS.ANO
AND PLAN_DE_COMPRAS.CODIGO        = DETALLE_PLAN_COMPRAS.RUBRO
AND PLAN_DE_COMPRAS.COMPANIA      = DETALLE_PLAN_COMPRAS.COMPANIA
INNER JOIN D_ORDENDECOMPRA
ON DETALLE_PLAN_COMPRAS.COMPANIA = D_ORDENDECOMPRA.COMPANIA
AND DETALLE_PLAN_COMPRAS.CODIGO  = D_ORDENDECOMPRA.ELEMENTO
AND DETALLE_PLAN_COMPRAS.RUBRO   = D_ORDENDECOMPRA.RUBROPPTO
INNER JOIN ORDENDECOMPRA
ON D_ORDENDECOMPRA.COMPANIA       = ORDENDECOMPRA.COMPANIA
AND D_ORDENDECOMPRA.CLASEORDEN    = ORDENDECOMPRA.CLASEORDEN
AND D_ORDENDECOMPRA.ORDENDECOMPRA = ORDENDECOMPRA.NUMERO
LEFT JOIN EJECUTADO 
ON (PLAN_DE_COMPRAS.COMPANIA = EJECUTADO.COMPANIA
  AND PLAN_DE_COMPRAS.CODIGO = EJECUTADO.RUBRO
  AND PLAN_DE_COMPRAS.ANO = EJECUTADO.ANO)
  WHERE PLAN_DE_COMPRAS.COMPANIA = s$compania$s
  AND PLAN_DE_COMPRAS.ANO = s$ano$s
  AND EXTRACT (YEAR FROM  ORDENDECOMPRA.FECHA) = s$ano$s
GROUP BY PLAN_DE_COMPRAS.COMPANIA,
  PLAN_DE_COMPRAS.CODIGO,
  PLAN_DE_COMPRAS.ANO,
  PLAN_DE_COMPRAS.NOMBRE,
  D_ORDENDECOMPRA.CANTIDAD,
  D_ORDENDECOMPRA.VALORUNITARIODI,
  D_ORDENDECOMPRA.VLRTOTAL,
  INVENTARIO.NOMBRELARGO,
  DETALLE_PLAN_COMPRAS.CODIGO,
  INVENTARIO.CUBS,
  D_ORDENDECOMPRA.CLASEORDEN,
  D_ORDENDECOMPRA.ORDENDECOMPRA,
  ORDENDECOMPRA.FECHA,
  EXTRACT (YEAR FROM ORDENDECOMPRA.FECHA),
  EJECUTADO.SUMADEVALORTOTALCOMPRADO
