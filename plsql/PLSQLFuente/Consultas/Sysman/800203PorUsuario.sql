SELECT 
  UPPER(APLICACIONES.NOMBRE) NOMBRE_APLICACION,
  UPPER( CASE WHEN  NOMBRE1 IS NULL THEN '' ELSE NOMBRE1 ||' ' END ||
  CASE WHEN  NOMBRE2 IS NULL THEN '' ELSE  NOMBRE2 ||' ' END ||
  CASE WHEN  APELLIDO1 IS NULL THEN '' ELSE APELLIDO1 ||' ' END ||
  CASE WHEN  APELLIDO2 IS NULL THEN '' ELSE APELLIDO2 END) NOMBRE,
  UPPER(USUARIO_D.GRUPO) GRUPO,
  CASE WHEN USUARIO.ESTADO IN('A') THEN 'ACTIVO' ELSE 'RETIRADO' END ESTADO
FROM USUARIO_D
INNER JOIN USUARIO
ON USUARIO_D.USUARIO = USUARIO.CODIGO
INNER JOIN GRUPO_MENU
ON USUARIO_D.APLICACION = GRUPO_MENU.APLICACION
INNER JOIN APLICACIONES
ON USUARIO_D.APLICACION = APLICACIONES.APLICACION
WHERE GRUPO_MENU.COMPANIA = 's$compania$s'
GROUP BY
  CASE WHEN  NOMBRE1 IS NULL THEN '' ELSE NOMBRE1 ||' ' END ||
  CASE WHEN  NOMBRE2 IS NULL THEN '' ELSE  NOMBRE2 ||' ' END ||
  CASE WHEN  APELLIDO1 IS NULL THEN '' ELSE APELLIDO1 ||' ' END ||
  CASE WHEN  APELLIDO2 IS NULL THEN '' ELSE APELLIDO2 END,
  UPPER(APLICACIONES.NOMBRE),
  USUARIO_D.GRUPO,
  CASE WHEN USUARIO.ESTADO IN('A') THEN 'ACTIVO' ELSE 'RETIRADO' END 
ORDER BY 
  UPPER(APLICACIONES.NOMBRE)
