SELECT 
  UPPER(APLICACIONES.NOMBRE) NOMBRE_APLICACION,
  UPPER(GRUPO_MENU.GRUPO) GRUPO,
  MENU.NOMBRE OPCIONES_MENU,
  UPPER(FORMULARIO_APLICACION.RUTA)  RUTA_ACCESO,
  CASE WHEN GRUPO_MENU.VER IN(-1) THEN 'ACTIVO' ELSE 'INACTIVO' END ESTADO,
  CASE WHEN GRUPO_FORMULARIO.CONSULTAR IN(-1) THEN 'SI' ELSE 'NO' END PERMITE_CONSULTAR,
  CASE WHEN GRUPO_FORMULARIO.ADICIONAR IN(-1) THEN 'SI' ELSE 'NO' END PERMITE_ADICIONAR,
  CASE WHEN GRUPO_FORMULARIO.MODIFICAR IN(-1) THEN 'SI' ELSE 'NO' END PERMITE_MODIFICAR,
  CASE WHEN GRUPO_FORMULARIO.BORRAR IN(-1) THEN 'SI' ELSE 'NO' END PERMITE_BORRAR,
  CASE WHEN GRUPO_FORMULARIO.EXPORTAR IN(-1) THEN 'SI' ELSE 'NO' END PERMITE_EXPORTAR
  FROM GRUPO_MENU 
  INNER JOIN MENU
  ON MENU.APLICACION  = GRUPO_MENU.APLICACION
  AND MENU.CODIGO     = GRUPO_MENU.MENU
  INNER JOIN FORMULARIO
  ON MENU.PARAMETRO = TO_CHAR(FORMULARIO.CODIGO_FORMULARIO)
  INNER JOIN FORMULARIO_APLICACION
  ON FORMULARIO.CODIGO_FORMULARIO = FORMULARIO_APLICACION.FORMULARIO
  INNER JOIN APLICACIONES
  ON FORMULARIO_APLICACION.APLICACION = APLICACIONES.APLICACION
  INNER JOIN GRUPO_FORMULARIO
  ON FORMULARIO_APLICACION.APLICACION  = GRUPO_FORMULARIO.APLICACION
  AND FORMULARIO_APLICACION.FORMULARIO = GRUPO_FORMULARIO.FORMULARIO
  WHERE GRUPO_MENU.COMPANIA  = 's$compania$s'
  AND GRUPO_FORMULARIO.COMPANIA = 's$compania$s'
  AND UPPER(FORMULARIO_APLICACION.RUTA) IS NOT NULL
  ORDER BY UPPER(APLICACIONES.NOMBRE) , 
  TO_CHAR(MENU.CODIGO),
  MENU.CODIGO 
