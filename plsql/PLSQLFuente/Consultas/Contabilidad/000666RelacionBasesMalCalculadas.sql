SELECT COMPROBANTE_CNT.COMPANIA, 
COMPROBANTE_CNT.TIPO, 
COMPROBANTE_CNT.NUMERO, 
COMPROBANTE_CNT.VLR_BASE BASE, 
COMPROBANTE_CNT.VLR_BASEIVA BASEIVA, 
COMPROBANTE_CNT.FECHA, 
COMPROBANTE_CNT.DESCRIPCION, 
COMPROBANTE_CNT.NRO_DOCUMENTO NUMDOC, 
COMPROBANTE_CNT.VLR_DOCUMENTO VALOR, 
TIPO_COMPROBANTE.NOMBRE NOMBRECOMP, 
TERCERO.NOMBRE NOMBRETER, 
PCK_SYSMAN_UTL.FC_ROUND(VLR_BASEIVA,0) BASEIVAREDONDEO
FROM COMPROBANTE_CNT 
	INNER JOIN TIPO_COMPROBANTE 
		ON COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA 
		AND COMPROBANTE_CNT.TIPO = TIPO_COMPROBANTE.CODIGO
	INNER JOIN TERCERO 
		ON COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA
		AND COMPROBANTE_CNT.TERCERO = TERCERO.NIT
		AND COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL
WHERE COMPROBANTE_CNT.COMPANIA=s$compania$s
	AND COMPROBANTE_CNT.VLR_BASE<>0 
	AND TIPO_COMPROBANTE.PIDE_RETENCION <> 0 
	AND FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
	AND ((COMPROBANTE_CNT.VLR_BASEIVA <> VLR_BASE*0.16 
		AND PCK_SYSMAN_UTL.FC_ROUND(VLR_BASEIVA,0)<>PCK_SYSMAN_UTL.FC_ROUND(VLR_BASE*0.16,0)
		AND TERCERO.REGIMEN IN ('C','G'))
	OR (COMPROBANTE_CNT.VLR_BASEIVA <> 0
		AND PCK_SYSMAN_UTL.FC_ROUND(VLR_BASEIVA,0)<>0
		AND TERCERO.REGIMEN NOT IN ('C','G')))
