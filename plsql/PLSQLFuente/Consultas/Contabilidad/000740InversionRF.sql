SELECT INVERSIONRF.CODIGO,
       INVERSIONRF.FONDO,
       FUENTE_RECURSOS.NOMBRE ORIGENRECURSOS,
       INVERSIONRF.ENTIDADEMISORA,
       INVERSIONRF.VALORNOMINAL,
       INVERSIONRF.TASA,
       INVERSIONRF.VENCIDO,
       INVERSIONRF.ANTICIPADO,
       INVERSIONRF.RENDIMIENTOSFINANCIEROS,
       INVERSIONRF.FECHACOLOCACION,
       INVERSIONRF.FECHAVENCIMIENTO,
       INVERSIONRF.SALDOFINAL,
       DECODE(INVERSIONRF.REINVERSION, '0' , ' ' , 'X')REINVERSION
FROM INVERSIONRF
  LEFT JOIN FUENTE_RECURSOS
    ON INVERSIONRF.COMPANIA         = FUENTE_RECURSOS.COMPANIA
    AND INVERSIONRF.ANO             = FUENTE_RECURSOS.ANO
    AND INVERSIONRF.ORIGENRECURSOS  = FUENTE_RECURSOS.CODIGO
WHERE INVERSIONRF.COMPANIA = s$compania$s 
AND INVERSIONRF.FECHACOLOCACION BETWEEN s$fechaInicial$s AND s$fechaFinal$s
