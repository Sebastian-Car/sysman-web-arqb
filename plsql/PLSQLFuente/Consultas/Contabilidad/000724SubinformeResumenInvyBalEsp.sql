SELECT SALDOAUX.COMPANIA,  
       SALDOAUX.CODIGO, 
       SUBSTR(SALDOAUX.CODIGO, 1, 1)  CLASE,  
       SALDOAUX.NOMBRE, 
       CASE WHEN SALDOAUX.NATURALEZA = 'C' AND  SALDOAUX.SALDOs$mesTrabajo$s >= 0 THEN  SALDOAUX.SALDOs$mesTrabajo$s ELSE 0 END +  
       CASE WHEN SALDOAUX.NATURALEZA = 'D' AND  SALDOAUX.SALDOs$mesTrabajo$s < 0 THEN  -  SALDOAUX.SALDOs$mesTrabajo$s ELSE 0 END  SALDONUECREDITO,  
       CASE WHEN SALDOAUX.NATURALEZA = 'D' AND  SALDOAUX.SALDOs$mesTrabajo$s >= 0 THEN  SALDOAUX.SALDOs$mesTrabajo$s ELSE 0 END + 
       CASE WHEN SALDOAUX.NATURALEZA = 'C' AND  SALDOAUX.SALDOs$mesTrabajo$s < 0 THEN -  SALDOAUX.SALDOs$mesTrabajo$s ELSE 0 END  SALDONUEDEBITO, 
       SALDOAUX.SALDOs$mesTrabajo$s AS SALDO, 
       SALDOAUX.NATURALEZA,  
       LPAD((CASE WHEN (SUBSTR(SALDOAUX.CODIGO, 1, 1)) = '0' THEN 'Z' || SALDOAUX.CODIGO ELSE SALDOAUX.CODIGO END), 1)  CLASEORDEN 
FROM (s$baseBalance$s) SALDOAUX 
WHERE  LENGTH(SALDOAUX.CODIGO) = 1
