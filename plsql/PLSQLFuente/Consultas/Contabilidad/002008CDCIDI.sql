SELECT 
  VDT.TIPO_COMPROBANTE_NOM AS NOMBRE
  , VDT.COMPROBANTE AS NUMERO
  , VDT.TIPO_COMPROBANTE_NOM
  , VDT.TERCERO_COM_NOM AS NOMBRETERCERO1
  , VDT.TERCERO_COM  AS TERCERO
  , VDT.DIRECCION_DET AS TERCERODIRE
  , CASE
		WHEN VDT.CODIGOPOSTAL IS NULL
		OR VDT.CODIGOPOSTAL    =''
		THEN PCK_SYSMAN_UTL.FC_PAR(VDT.COMPANIA,'CODIGO POSTAL',1,SYSDATE,-1)
		ELSE VDT.CODIGOPOSTAL
	  END CODIGOPOSTAL 
  , VDT.TELEFONOS
  , CASE
    WHEN TO_NUMBER(VDT.VLRAGIRAR) = 1
    THEN 0
    ELSE VDT.VLRAGIRAR
  END SUMADE
  , TO_CHAR(VDT.FECHA,'DD/MM/YYYY') FECHA
  , VDT.CENTRO_COSTO
  , VDT.VLR_DOCUMENTO
  , VDT.CONTRIBUYENTE
  , VDT.DESCRIPCION
  , VDT.TEXTO
  , VDT.CUENTAPPTAL
  , VDT.CODIGO_CUENTA
  , VDT.CUENTANOMBRE AS TOTALNOMBRE
  , VDT.VALOR_DEBITO
  , VDT.VALOR_CREDITO
  , VDT.NRO_DOCUMENTO 
  , VDT.NRO_DOCUMENTO_DET AS NRO_DOCUMENTO_DCNT
  , VDT.FECHA_VCN_DOC
  , VDT.VLRAGIRAR
  , CASE WHEN VALOR_DEBITO <> 0 THEN 'A' || CODIGO_CUENTA ELSE 'Z' || CODIGO_CUENTA END SEC
  , VDT.CREATED_BY,
  NVL(USUARIO.NOMBRE1,'') ||' '||
  NVL(USUARIO.NOMBRE2,'') ||' '||
  NVL(USUARIO.APELLIDO1,'') ||' '||
  NVL(USUARIO.APELLIDO2,'')NOMBREUSUARIO
FROM V_DETALLE_AUXILIAR_CNT VDT
LEFT JOIN USUARIO
ON VDT.CREATED_BY  = USUARIO.CODIGO
WHERE VDT.COMPANIA = s$compania$s
AND VDT.ANO = s$ano$s 
AND VDT.TIPO_CPTE = 's$tipoCpte$s'
AND VDT.COMPROBANTE BETWEEN  s$numeroPptoInicial$s AND s$numeroPptoFinal$s