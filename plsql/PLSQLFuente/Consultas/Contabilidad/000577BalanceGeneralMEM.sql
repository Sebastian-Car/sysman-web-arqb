SELECT  
SUBSTR(V_PLAN_CONTABLE.CODIGO,1,1) AS CLASE,  
V_PLAN_CONTABLE.COMPANIA,  
V_PLAN_CONTABLE.CODIGO,  
V_PLAN_CONTABLE.NOMBRE AS PLAN_CONTABLE_NOMBRE,  
(CASE WHEN (V_PLAN_CONTABLE.NATURALEZA='D' AND V_PLAN_CONTABLE.SALDOs$mestrabajo$s>=0) THEN V_PLAN_CONTABLE.SALDOs$mestrabajo$s ELSE 0 END)+ (CASE WHEN (V_PLAN_CONTABLE.NATURALEZA='C' AND V_PLAN_CONTABLE.SALDOs$mestrabajo$s<0) THEN -V_PLAN_CONTABLE.SALDOs$mestrabajo$s ELSE 0 END) AS SALDONUEDEBITO,  
(CASE WHEN (V_PLAN_CONTABLE.NATURALEZA='C' AND V_PLAN_CONTABLE.SALDOs$mestrabajo$s>=0) THEN V_PLAN_CONTABLE.SALDOs$mestrabajo$s ELSE 0 END)+ (CASE WHEN(V_PLAN_CONTABLE.NATURALEZA='D' AND V_PLAN_CONTABLE.SALDOs$mestrabajo$s<0) THEN -V_PLAN_CONTABLE.SALDOs$mestrabajo$s ELSE 0 END) AS SALDONUECREDITO, 
SUBSTR((CASE WHEN (SUBSTR(V_PLAN_CONTABLE.CODIGO,1,1)='0') THEN 'z' || V_PLAN_CONTABLE.CODIGO ELSE V_PLAN_CONTABLE.CODIGO END),1,1) AS CLASEORDEN 
FROM V_PLAN_CONTABLE  
WHERE V_PLAN_CONTABLE.COMPANIA = s$compania$s   
AND V_PLAN_CONTABLE.ANO =   s$anotrabajo$s 
AND V_PLAN_CONTABLE.CODIGO >= 's$codInicial$s' 
AND V_PLAN_CONTABLE.CODIGO <= 's$codFinal$s'  
AND LENGTH(V_PLAN_CONTABLE.CODIGO)<=  s$digitos$s  
s$condicion$s 
ORDER BY  V_PLAN_CONTABLE.ID
