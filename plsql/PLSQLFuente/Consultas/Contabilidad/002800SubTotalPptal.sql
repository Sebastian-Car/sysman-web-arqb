MERGE INTO CONSULTAS FIN USING (SELECT '002800SubTotalPptal' INFORME ,TO_CLOB(q'[SELECT
    DETALLE_COMPROBANTE_CNT.COMPANIA,
    DETALLE_COMPROBANTE_CNT.ANO,
    NVL(RESUMENPPTO_PGAITAN.TOTALINGRESOS, 0) AS TOTALINGRESOS
FROM
    DETALLE_COMPROBANTE_CNT
    LEFT JOIN TIPO_COMPROBANTE ON DETALLE_COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA
        AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
    LEFT JOIN V_PLAN_CONTABLE ON DETALLE_COMPROBANTE_CNT.COMPANIA = V_PLAN_CONTABLE.COMPANIA
        AND DETALLE_COMPROBANTE_CNT.ANO = V_PLAN_CONTABLE.ANO
        AND DETALLE_COMPROBANTE_CNT.CUENTA = V_PLAN_CONTABLE.ID
    LEFT JOIN (
        SELECT
            SALDO_PLAN_PPTAL.COMPANIA,
            SALDO_PLAN_PPTAL.ANO,
            SALDO_PLAN_PPTAL.CODIGO,
            SUM(
                CASE
                    WHEN V_PLAN_PRESUPUESTAL.NATURALEZA = 'D' THEN
                        EJE_PPT_DEBITO - EJE_PPT_CREDITO
                    ELSE
                        EJE_PPT_CREDITO - EJE_PPT_DEBITO
                END
            ) + SUM(SALDO_PLAN_PPTAL.MODIF_INGRESOS) AS TOTALINGRESOS
        FROM
            V_PLAN_PRESUPUESTAL
            LEFT JOIN SALDO_PLAN_PPTAL ON V_PLAN_PRESUPUESTAL.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
                AND V_PLAN_PRESUPUESTAL.ANO = SALDO_PLAN_PPTAL.ANO
                AND V_PLAN_PRESUPUESTAL.ID = SALDO_PLAN_PPTAL.CODIGO
       WHERE SALDO_PLAN_PPTAL.COMPANIA=s$compania$s
                AND SALDO_PLAN_PPTAL.ANO=EXTRACT (YEAR FROM (TO_DATE('s$fechaFinal$s','DD/MM/YYYY')))
                AND SALDO_PLAN_PPTAL.MES BETWEEN EXTRACT (MONTH FROM (TO_DATE('s$fechaInicial$s','DD/MM/YYYY'))) AND EXTRACT (MONTH FROM (TO_DATE('s$fechaFinal$s','DD/MM/YYYY')))
              GROUP BY
            SALDO_PLAN_PPTAL.COMPANIA,
            SALDO_PLAN_PPTAL.ANO,
            SALDO_PLAN_PPTAL.CODIGO
    ) RESUMENPPTO_PGAITAN ON DETALLE_COMPROBANTE_CNT.COMPANIA = RESUMENPPTO_PGAITAN.COMPANIA
        AND DETALLE_COMPROBANTE_CNT.ANO = RESUMENPPTO_PGAITAN.ANO
        AND DETALLE_COMPROBANTE_CNT.CUENTAPPTAL = RESUMENPPTO_PGAITAN.CODIGO
WHERE
    DETALLE_COMPROBANTE_CNT.COMPANIA = s$compania$s
    AND DETALLE_COMPROBANTE_CNT.CUENTA BETWEEN 's$cuentaInicial$s' AND  's$cuentaFinal$s'
    AND DETALLE_COMPROBANTE_CNT.FECHA BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
    AND TIPO_COMPROBANTE.CLASE_CONTABLE IN ('I', 'S', 'B', 'J')
GROUP BY
    DETALLE_COMPROBANTE_CNT.COMPANIA,
    DETALLE_COMPROBANTE_CNT.ANO,
    DETALLE_COMPROBANTE_CNT.CUENTAPPTAL,
    RESUMENPPTO_PGAITAN.TOTALINGRESOS]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);