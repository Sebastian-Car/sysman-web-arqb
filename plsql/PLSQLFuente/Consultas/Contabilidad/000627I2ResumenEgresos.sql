SELECT 
  DETALLE_COMPROBANTE_CNT.COMPANIA,
  DETALLE_COMPROBANTE_CNT.ANO,
  DETALLE_COMPROBANTE_CNT.CUENTA CODIGO_CUENTA,
  SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO) SUMADEVALOR_DEBITO,
  SUM(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO) SUMADEVALOR_CREDITO,
  V_PLAN_CONTABLE.NOMBRE
FROM DETALLE_COMPROBANTE_CNT 
  LEFT JOIN TIPO_COMPROBANTE 
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
  LEFT JOIN PLAN_CONTABLE  V_PLAN_CONTABLE 
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA = V_PLAN_CONTABLE.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.ANO = V_PLAN_CONTABLE.ANO
    AND DETALLE_COMPROBANTE_CNT.CUENTA = V_PLAN_CONTABLE.CODIGO
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA=s$compania$s
  AND TIPO_COMPROBANTE.CLASE_CONTABLE IN (s$clasesContables$s) s$claseCuenta$s
  AND DETALLE_COMPROBANTE_CNT.CUENTA BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  AND TRUNC(DETALLE_COMPROBANTE_CNT.FECHA) BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
GROUP BY DETALLE_COMPROBANTE_CNT.COMPANIA, DETALLE_COMPROBANTE_CNT.ANO, DETALLE_COMPROBANTE_CNT.CUENTA, V_PLAN_CONTABLE.NOMBRE
