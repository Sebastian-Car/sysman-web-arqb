WITH BALANCESALDOSINICIALES5N AS 
(
    SELECT 
      SALDOSINICIALES.COMPANIA,
      SALDOSINICIALES.ANO,
      SALDOSINICIALES.CODIGO SALDOS_INICIALES_CODIGO,      
      CASE WHEN SALDOSINICIALES.AUXILIAR IS NOT NULL THEN AUXILIAR.NOMBRE
      ELSE
        CASE WHEN SALDOSINICIALES.TERCERO IS NOT NULL THEN TERCERO.NOMBRE 
        ELSE
          CASE WHEN SALDOSINICIALES.CENTRO_COSTO IS NOT NULL THEN CENTRO_COSTO.NOMBRE 
          ELSE PLAN_CONTABLE.NOMBRE 
          END
        END
      END NOMBRE,      
      SALDOSINICIALES.SALDOINICIAL,  
      SUBSTR(SALDOSINICIALES.CODIGO,1,1) CLASE,
      SUBSTR(SALDOSINICIALES.CODIGO,1,2) GRUPO,
      SUBSTR(SALDOSINICIALES.CODIGO,1,4) CUENTA,
      SUBSTR(SALDOSINICIALES.CODIGO,1,6) SUBCUENTA, 
      SUBSTR(SALDOSINICIALES.CODIGO,1,8) SUBAUXILIAR,
      SALDOSINICIALES.TERCERO,
      CENTRO_COSTO.CODIGO CENTRO_COSTO_CODIGO,
      AUXILIAR.CODIGO AUXILIAR_CODIGO  
    FROM SALDOSINICIALES
    INNER JOIN PLAN_CONTABLE
       ON SALDOSINICIALES.COMPANIA  = PLAN_CONTABLE.COMPANIA
      AND SALDOSINICIALES.ANO      = PLAN_CONTABLE.ANO 
      AND SALDOSINICIALES.CODIGO = PLAN_CONTABLE.CODIGO
    INNER JOIN TERCERO
	  ON  SALDOSINICIALES.COMPANIA  = TERCERO.COMPANIA 
	 AND  SALDOSINICIALES.TERCERO  = TERCERO.NIT 
     AND  SALDOSINICIALES.SUCURSAL = TERCERO.SUCURSAL  
    INNER JOIN CENTRO_COSTO
	  ON SALDOSINICIALES.COMPANIA    = CENTRO_COSTO.COMPANIA
     AND  SALDOSINICIALES.ANO      = CENTRO_COSTO.ANO 
     AND SALDOSINICIALES.CENTRO_COSTO = CENTRO_COSTO.CODIGO
    INNER JOIN AUXILIAR
      ON SALDOSINICIALES.COMPANIA     = AUXILIAR.COMPANIA
     AND  SALDOSINICIALES.ANO      = AUXILIAR.ANO
     AND SALDOSINICIALES.AUXILIAR    = AUXILIAR.CODIGO
    WHERE SALDOSINICIALES.COMPANIA = s$compania$s  
     AND SALDOSINICIALES.ANO        = s$anio$s   
    ORDER BY SALDOSINICIALES.CODIGO,
      CASE WHEN SALDOSINICIALES.AUXILIAR IS NOT NULL THEN AUXILIAR.NOMBRE
      ELSE
        CASE WHEN SALDOSINICIALES.TERCERO IS NOT NULL THEN TERCERO.NOMBRE 
        ELSE
          CASE WHEN SALDOSINICIALES.CENTRO_COSTO IS NOT NULL THEN CENTRO_COSTO.NOMBRE 
          ELSE PLAN_CONTABLE.NOMBRE 
          END
        END
      END
)
SELECT 
  BALANCESALDOSINICIALES5N.SALDOS_INICIALES_CODIGO,
  BALANCESALDOSINICIALES5N.NOMBRE,
  BALANCESALDOSINICIALES5N.SALDOINICIAL,
  BALANCESALDOSINICIALES5N.CLASE,
  BALANCESALDOSINICIALES5N.GRUPO,
  BALANCESALDOSINICIALES5N.CUENTA,
  BALANCESALDOSINICIALES5N.SUBCUENTA,
  BALANCESALDOSINICIALES5N.SUBAUXILIAR,
  NVL(PLAN_CONTABLE.NOMBRE,'  ') NOMBRECLASE, 
  NVL(PLAN_CONTABLE_1.NOMBRE,'  ') NOMBRE1,
  NVL(PLAN_CONTABLE_2.NOMBRE,'  ') NOMBRE2,
  NVL(PLAN_CONTABLE_3.NOMBRE,'  ') NOMBRE3,
  NVL(PLAN_CONTABLE_4.NOMBRE,'  ') NOMBRE4 
FROM BALANCESALDOSINICIALES5N
LEFT JOIN PLAN_CONTABLE
  ON  BALANCESALDOSINICIALES5N.COMPANIA = PLAN_CONTABLE.COMPANIA 
 AND  BALANCESALDOSINICIALES5N.ANO     = PLAN_CONTABLE.ANO 
 AND  BALANCESALDOSINICIALES5N.CLASE   = PLAN_CONTABLE.CODIGO  
LEFT JOIN PLAN_CONTABLE PLAN_CONTABLE_1
  ON  BALANCESALDOSINICIALES5N.COMPANIA = PLAN_CONTABLE_1.COMPANIA 
 AND  BALANCESALDOSINICIALES5N.ANO      = PLAN_CONTABLE_1.ANO  
 AND BALANCESALDOSINICIALES5N.GRUPO     = PLAN_CONTABLE_1.CODIGO
LEFT JOIN PLAN_CONTABLE PLAN_CONTABLE_2
  ON BALANCESALDOSINICIALES5N.COMPANIA = PLAN_CONTABLE_2.COMPANIA 
 AND  BALANCESALDOSINICIALES5N.ANO      = PLAN_CONTABLE_2.ANO  
 AND BALANCESALDOSINICIALES5N.CUENTA    = PLAN_CONTABLE_2.CODIGO
LEFT JOIN PLAN_CONTABLE PLAN_CONTABLE_3
  ON BALANCESALDOSINICIALES5N.COMPANIA = PLAN_CONTABLE_3.COMPANIA 
 AND  BALANCESALDOSINICIALES5N.ANO      = PLAN_CONTABLE_3.ANO  
 AND BALANCESALDOSINICIALES5N.SUBCUENTA = PLAN_CONTABLE_3.CODIGO
LEFT JOIN PLAN_CONTABLE PLAN_CONTABLE_4
  ON  BALANCESALDOSINICIALES5N.COMPANIA    = PLAN_CONTABLE_4.COMPANIA 
 AND BALANCESALDOSINICIALES5N.ANO          = PLAN_CONTABLE_4.ANO 
 AND  BALANCESALDOSINICIALES5N.SUBAUXILIAR = PLAN_CONTABLE_4.CODIGO	 
