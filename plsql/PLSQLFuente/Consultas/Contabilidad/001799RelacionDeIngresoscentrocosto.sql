SELECT 
	DETALLE_COMPROBANTE_CNT.CUENTA CODIGO_CUENTA,
	SUM (DETALLE_COMPROBANTE_CNT.VALOR_DEBITO)           SUMADEVALOR_DEBITO,
	SUM (DETALLE_COMPROBANTE_CNT.VALOR_CREDITO)          SUMADEVALOR_CREDITO,
	PLAN_CONTABLE.NOMBRE                                    ULTIMODENOMBRE,
	PLAN_PRESUPUESTAL.NOMBRE                             ULTIMODENOMBRE1,
	PLAN_PRESUPUESTAL.CODIGO                             ULTIMODECODIGO,
	DETALLE_COMPROBANTE_CNT.COMPANIA,
	TIPO_COMPROBANTE.CLASE_CONTABLE,
	CENTRO_COSTO.CODIGO CENTRO_COSTO
         
FROM DETALLE_COMPROBANTE_CNT
	INNER JOIN CENTRO_COSTO
		ON DETALLE_COMPROBANTE_CNT.COMPANIA = CENTRO_COSTO.COMPANIA
		AND DETALLE_COMPROBANTE_CNT.ANO    = CENTRO_COSTO.ANO
		AND DETALLE_COMPROBANTE_CNT.CENTRO_COSTO = CENTRO_COSTO.CODIGO
	INNER JOIN PLAN_CONTABLE
		ON DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA
		AND DETALLE_COMPROBANTE_CNT.CUENTA  = PLAN_CONTABLE.CODIGO
		AND DETALLE_COMPROBANTE_CNT.ANO     = PLAN_CONTABLE.ANO
	LEFT JOIN PLAN_PRESUPUESTAL
		ON PLAN_CONTABLE.COMPANIA       = PLAN_PRESUPUESTAL.COMPANIA
		AND PLAN_CONTABLE.CUENTA_PPTAL  = PLAN_PRESUPUESTAL.CODIGO
		AND PLAN_CONTABLE.ANO  			= PLAN_PRESUPUESTAL.ANO
	INNER JOIN TIPO_COMPROBANTE
		ON DETALLE_COMPROBANTE_CNT.COMPANIA   = TIPO_COMPROBANTE.COMPANIA
		AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA  = s$compania$s
		AND NVL (DETALLE_COMPROBANTE_CNT.CENTRO_COSTO,'') BETWEEN 's$centroInicial$s' AND 's$centroFinal$s'
		AND DETALLE_COMPROBANTE_CNT.FECHA = s$fecha$s 
		AND ((SUBSTR (DETALLE_COMPROBANTE_CNT.CUENTA,2)) >= '12')
		AND TIPO_COMPROBANTE.CLASE_CONTABLE IN ('I','S')
GROUP BY DETALLE_COMPROBANTE_CNT.CUENTA,
	PLAN_CONTABLE.NOMBRE,
	PLAN_PRESUPUESTAL.NOMBRE,
	PLAN_PRESUPUESTAL.CODIGO,
	DETALLE_COMPROBANTE_CNT.COMPANIA,
	TIPO_COMPROBANTE.CLASE_CONTABLE,
	CENTRO_COSTO.CODIGO
