MERGE INTO CONSULTAS FIN USING (SELECT '002852MovimientoDeBancosPorCuenta' INFORME ,TO_CLOB(q'[SELECT
   DETALLE_COMPROBANTE_CNT.CUENTA        CUENTA
  ,PLAN_CONTABLE.NOMBRE                  NOMBRE_CUENTA
  ,TERCERO.NIT                           NIT_TERCERO
  ,TERCERO.NOMBRE                        NOMBRETERCERO
  ,DETALLE_COMPROBANTE_CNT.TIPO_CPTE 
  ,DETALLE_COMPROBANTE_CNT.COMPROBANTE
  ,TO_CHAR(DETALLE_COMPROBANTE_CNT.FECHA, 'DD/MM/YYYY')         FECHACOMP
   ,CASE 
            WHEN DETALLE_COMPROBANTE_CNT.VALOR_DEBITO <> 0 
                 THEN ROUND(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO, 2)
            WHEN DETALLE_COMPROBANTE_CNT.VALOR_CREDITO <> 0 
                 THEN -ROUND(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO, 2)
            ELSE 0
       END AS VALOR
  ,DETALLE_COMPROBANTE_CNT.DESCRIPCION   DESCRIP
FROM DETALLE_COMPROBANTE_CNT
  INNER JOIN PLAN_CONTABLE
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.ANO      = PLAN_CONTABLE.ANO
    AND DETALLE_COMPROBANTE_CNT.CUENTA   = PLAN_CONTABLE.CODIGO
  INNER JOIN TERCERO
    ON DETALLE_COMPROBANTE_CNT.COMPANIA  = TERCERO.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.TERCERO  = TERCERO.NIT
    AND DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL
WHERE PLAN_CONTABLE.COMPANIA = s$compania$s
  AND PLAN_CONTABLE.ANO      = s$anio$s
  AND PLAN_CONTABLE.CODIGO              BETWEEN s$codIni$s   AND s$codFin$s
  AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE BETWEEN s$tipoIni$s  AND s$tipoFin$s
  AND DETALLE_COMPROBANTE_CNT.FECHA     BETWEEN s$fechaIni$s AND s$fechaFin$s
    AND PLAN_CONTABLE.CLASECUENTA IN('B')
  AND (DETALLE_COMPROBANTE_CNT.VALOR_DEBITO  NOT IN(0)
    OR DETALLE_COMPROBANTE_CNT.VALOR_CREDITO NOT IN(0))]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
