MERGE INTO CONSULTAS FIN USING (SELECT '002540INFORETENCIONXCUENTA' INFORME ,TO_CLOB(q'[SELECT PLAN_CONTABLE.COMPANIA, PLAN_CONTABLE.CODIGO, PLAN_CONTABLE.NOMBRE CONCEPTO,SUM(BASE_GRAVABLE)VALOR_BASE,SUM(VALORTOTAL)VALOR_RETEFTE
FROM PLAN_CONTABLE INNER JOIN  (
    SELECT DETALLE_COMPROBANTE_CNT.COMPANIA,
           RETENCIONES.CUENTA_CREDITO AS ID,
           DETALLE_COMPROBANTE_CNT.ANO, 
           RETENCIONES.NOMBRE AS CONCEPTO,
           DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE, 
           COMPROBANTE_CNTRETENCION.VALOR AS VALORTOTAL
      FROM DETALLE_COMPROBANTE_CNT
INNER JOIN COMPROBANTE_CNTRETENCION ON DETALLE_COMPROBANTE_CNT.COMPROBANTE = COMPROBANTE_CNTRETENCION.NUMERO
       AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = COMPROBANTE_CNTRETENCION.TIPO
       AND DETALLE_COMPROBANTE_CNT.ANO = COMPROBANTE_CNTRETENCION.ANO
       AND DETALLE_COMPROBANTE_CNT.COMPANIA = COMPROBANTE_CNTRETENCION.COMPANIA
INNER JOIN RETENCIONES ON COMPROBANTE_CNTRETENCION.CODIGORETENCION = RETENCIONES.CODIGO
       AND COMPROBANTE_CNTRETENCION.TIPORETENCION = RETENCIONES.TIPO
       AND COMPROBANTE_CNTRETENCION.ANO = RETENCIONES.ANO
       AND COMPROBANTE_CNTRETENCION.COMPANIA = RETENCIONES.COMPANIA
     WHERE DETALLE_COMPROBANTE_CNT.COMPANIA = s$compania$s 
       AND RETENCIONES.CUENTA_CREDITO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
       AND DETALLE_COMPROBANTE_CNT.FECHA BETWEEN  s$fechaInicial$s AND s$fechaFinal$s 
  GROUP BY DETALLE_COMPROBANTE_CNT.COMPANIA,RETENCIONES.CUENTA_CREDITO, RETENCIONES.NOMBRE, DETALLE_COMPROBANTE_CNT.ANO,DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE, COMPROBANTE_CNTRETENCION.VALOR) RETENCIONES
        ON PLAN_CONTABLE.COMPANIA = RETENCIONES.COMPANIA
       AND PLAN_CONTABLE.ANO = RETENCIONES.ANO
       AND PLAN_CONTABLE.CODIGO = SUBSTR(RETENCIONES.ID, 1, LENGTH(PLAN_CONTABLE.CODIGO))    
GROUP BY PLAN_CONTABLE.COMPANIA,PLAN_CONTABLE.CODIGO, PLAN_CONTABLE.NOMBRE
ORDER BY PLAN_CONTABLE.CODIGO]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, 'MPEREZ' CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);