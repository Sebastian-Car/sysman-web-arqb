SELECT PLAN_CONTABLE.CODIGO AS FORMULAID,      
       NVL(PLAN_CONTABLE.CODIGO_NIIF,0) AS CODIGO_NIIF,      
       SUM(PLAN_CONTABLE_1.SALDOs$cSaldo$s) AS VALORACTUAL,      
       SUM(PLAN_CONTABLE.SALDOs$cSaldo$s) AS VALORCOMPARA 
FROM V_PLAN_CONTABLE PLAN_CONTABLE INNER JOIN V_PLAN_CONTABLE PLAN_CONTABLE_1   
       ON PLAN_CONTABLE.COMPANIA = PLAN_CONTABLE_1.COMPANIA    
      AND PLAN_CONTABLE.ID      = PLAN_CONTABLE_1.ID  
WHERE PLAN_CONTABLE.COMPANIA = s$compania$s    
  AND PLAN_CONTABLE.ANO      = s$anioTrabajo$s   
  AND PLAN_CONTABLE.SALDOs$cSaldo$s NOT IN (0)    
  AND PLAN_CONTABLE_1.ANO    = s$anioComparar$s 
GROUP BY PLAN_CONTABLE.CODIGO, NVL(PLAN_CONTABLE.CODIGO_NIIF,0) 
ORDER BY PLAN_CONTABLE.CODIGO
