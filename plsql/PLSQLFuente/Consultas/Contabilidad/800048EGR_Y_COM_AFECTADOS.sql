SELECT COMPROBANTE_CNTAFECTADOS.COMPANIA,
  COMPROBANTE_CNTAFECTADOS.ANO,
  COMPROBANTE_CNTAFECTADOS.TIPO_CPTE,
  DETALLE_COMPROBANTE_CNT.FECHA,
  COMPROBANTE_CNTAFECTADOS.TIPO_CPTE_AFECT,
  COMPROBANTE_CNTAFECTADOS.COMPROBANTE,
  COUNT(COMPROBANTE_CNTAFECTADOS.COMPROBANTE) CONTEO,
  COMPROBANTE_CNTAFECTADOS.COMPROBANTE_AFECT CMPTE_AFECTADO
FROM COMPROBANTE_CNTAFECTADOS 
INNER JOIN DETALLE_COMPROBANTE_CNT
ON DETALLE_COMPROBANTE_CNT.COMPANIA     = COMPROBANTE_CNTAFECTADOS.COMPANIA
AND DETALLE_COMPROBANTE_CNT.ANO         = COMPROBANTE_CNTAFECTADOS.ANO
AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE   = COMPROBANTE_CNTAFECTADOS.TIPO_CPTE
AND DETALLE_COMPROBANTE_CNT.COMPROBANTE = COMPROBANTE_CNTAFECTADOS.COMPROBANTE
WHERE COMPROBANTE_CNTAFECTADOS.COMPANIA  =  s$compania$s 
  AND COMPROBANTE_CNTAFECTADOS.ANO       =  s$anio$s
  AND COMPROBANTE_CNTAFECTADOS.TIPO_CPTE = 'EGR' 
  AND FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
GROUP BY COMPROBANTE_CNTAFECTADOS.COMPANIA, 
COMPROBANTE_CNTAFECTADOS.ANO, 
COMPROBANTE_CNTAFECTADOS.TIPO_CPTE, 
DETALLE_COMPROBANTE_CNT.FECHA, 
COMPROBANTE_CNTAFECTADOS.TIPO_CPTE_AFECT, 
COMPROBANTE_CNTAFECTADOS.COMPROBANTE_AFECT 
,
  COMPROBANTE_CNTAFECTADOS.COMPROBANTE
HAVING COUNT(COMPROBANTE_CNTAFECTADOS.COMPROBANTE)>1
