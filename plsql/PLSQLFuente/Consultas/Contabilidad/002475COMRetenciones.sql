MERGE INTO CONSULTAS FIN USING (SELECT '002475COMRetenciones' INFORME ,TO_CLOB(q'[SELECT 
    RETENCIONES.NOMBRE,
    RETENCIONES.PCT_APLICAR,
    (CASE WHEN ALEY1450 <> 0 THEN VALORBASELEY1607 ELSE COMPROBANTE_CNTRETENCION.VALORBASE END) AS VALORBASE
FROM COMPROBANTE_CNTRETENCION LEFT JOIN RETENCIONES ON 
    COMPROBANTE_CNTRETENCION.COMPANIA             = RETENCIONES.COMPANIA
    AND COMPROBANTE_CNTRETENCION.ANO              = RETENCIONES.ANO
    AND COMPROBANTE_CNTRETENCION.TIPORETENCION    = RETENCIONES.TIPO
    AND COMPROBANTE_CNTRETENCION.CODIGORETENCION  = RETENCIONES.CODIGO
LEFT JOIN TIPORETENCION ON 
    COMPROBANTE_CNTRETENCION.TIPORETENCION  = TIPORETENCION.CODIGO
WHERE COMPROBANTE_CNTRETENCION.COMPANIA = $P{PR_COMPANIA}
    AND COMPROBANTE_CNTRETENCION.ANO    = $P{PR_ANO}
    AND COMPROBANTE_CNTRETENCION.TIPO   = $P{PR_TIPO}
    AND COMPROBANTE_CNTRETENCION.NUMERO BETWEEN $P{PR_NUMERO} AND $P{PR_NUMERO}
ORDER BY COMPROBANTE_CNTRETENCION.COMPANIA,
    COMPROBANTE_CNTRETENCION.ANO,
    COMPROBANTE_CNTRETENCION.TIPO,
    COMPROBANTE_CNTRETENCION.NUMERO,
    COMPROBANTE_CNTRETENCION.TIPORETENCION,
    COMPROBANTE_CNTRETENCION.CODIGORETENCION]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
commit;
