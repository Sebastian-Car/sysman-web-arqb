SELECT 			V_PLAN_CONTABLE.COMPANIA, 
				SUBSTR(V_PLAN_CONTABLE.CODIGO,1,1)      CLASE,  
				V_PLAN_CONTABLE.SALDOs$mesActual$s            SALDO,  
				V_PLAN_CONTABLE.COMPANIA,  
				V_PLAN_CONTABLE.ANO,  
				V_PLAN_CONTABLE.SALDOs$mesAnterior$s    SALDO_1,   
				V_PLAN_CONTABLE.CODIGO,  
				V_PLAN_CONTABLE.CENTRO_COSTO,  
				V_PLAN_CONTABLE.NOMBRE,  
				V_PLAN_CONTABLE.TERCERO,  
				V_PLAN_CONTABLE.SUCURSAL,  
				V_PLAN_CONTABLE.AUXILIAR,  
				V_PLAN_CONTABLE.DEBITOs$mesActual$s 			DEBITO , 
				V_PLAN_CONTABLE.CREDITOs$mesActual$s 			CREDITO,  
				V_PLAN_CONTABLE.DEBITOs$mesAnterior$s 	DEBITO_1, 
				V_PLAN_CONTABLE.CREDITOs$mesAnterior$s  CREDITO_1 ,  
				V_PLAN_CONTABLE.NATURALEZA,  
				V_PLAN_CONTABLE.ID, 
				V_PLAN_CONTABLE.NETOs$mesAnterior$s		NETO, 
				(CASE WHEN V_PLAN_CONTABLE.NATURALEZA = 'D' AND V_PLAN_CONTABLE.SALDOs$mesAnterior$s >= 0 THEN  V_PLAN_CONTABLE.SALDOs$mesAnterior$s  ELSE 0 END) + 
				(CASE WHEN V_PLAN_CONTABLE.NATURALEZA = 'C' AND V_PLAN_CONTABLE.SALDOs$mesAnterior$s  < 0 THEN -V_PLAN_CONTABLE.SALDOs$mesAnterior$s  ELSE 0 END) 
				SALDOANTDEBITO, 
				(CASE WHEN V_PLAN_CONTABLE.NATURALEZA = 'C' AND V_PLAN_CONTABLE.SALDOs$mesAnterior$s >= 0 THEN  V_PLAN_CONTABLE.SALDOs$mesAnterior$s  ELSE 0 END) + 
				(CASE WHEN V_PLAN_CONTABLE.NATURALEZA = 'D' AND V_PLAN_CONTABLE.SALDOs$mesAnterior$s  < 0 THEN -V_PLAN_CONTABLE.SALDOs$mesAnterior$s  ELSE 0 END) 
				SALDOANTCREDITO, 
				(CASE WHEN V_PLAN_CONTABLE.NATURALEZA = 'D' AND V_PLAN_CONTABLE.SALDOs$mesActual$s >= 0   THEN  V_PLAN_CONTABLE.SALDOs$mesActual$s    ELSE 0 END) + 
				(CASE WHEN V_PLAN_CONTABLE.NATURALEZA = 'C' AND V_PLAN_CONTABLE.SALDOs$mesActual$s  < 0   THEN -V_PLAN_CONTABLE.SALDOs$mesActual$s    ELSE 0 END) 
				SALDONUEDEBITO, 
				(CASE WHEN V_PLAN_CONTABLE.NATURALEZA = 'C' AND V_PLAN_CONTABLE.SALDOs$mesActual$s >= 0   THEN  V_PLAN_CONTABLE.SALDOs$mesActual$s    ELSE 0 END) + 
				(CASE WHEN V_PLAN_CONTABLE.NATURALEZA = 'D' AND V_PLAN_CONTABLE.SALDOs$mesActual$s  < 0   THEN -V_PLAN_CONTABLE.SALDOs$mesActual$s    ELSE 0 END) 
				SALDONUECREDITO, 
				CASE WHEN SUBSTR(V_PLAN_CONTABLE.CODIGO,1,1) = '0' THEN 'Z'||V_PLAN_CONTABLE.CODIGO ELSE V_PLAN_CONTABLE.CODIGO END ORDEN, 
				SUBSTR((CASE WHEN SUBSTR(V_PLAN_CONTABLE.CODIGO,1,1) = '0'  THEN 'Z'||V_PLAN_CONTABLE.CODIGO ELSE V_PLAN_CONTABLE.CODIGO END),1,1) CLASEORDEN 
	FROM        V_PLAN_CONTABLE 
	WHERE       V_PLAN_CONTABLE.COMPANIA  =  s$compania$s 
	AND 		V_PLAN_CONTABLE.ANO  =  s$ano$s 
	AND			V_PLAN_CONTABLE.CODIGO  BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 
	AND         LENGTH(V_PLAN_CONTABLE.CODIGO) = 1
