SELECT V_PLAN_CONTABLE.CODIGO,  
       V_PLAN_CONTABLE.NOMBRE  PLAN_CONTABLE_NOMBRE,  
       V_PLAN_CONTABLE.SALDOs$mesTrabajo$s  SALDO,  
       V_PLAN_CONTABLE.SALDOs$mesTrabajo1$s  SALDOANT, 
       V_PLAN_CONTABLE.NETOs$mesTrabajo$s  NETO, 
       V_PLAN_CONTABLE.COMPANIA, 
       LPAD((CASE WHEN SUBSTR(V_PLAN_CONTABLE.CODIGO, 1, 1) = '0' THEN 'Z' || V_PLAN_CONTABLE.CODIGO ELSE V_PLAN_CONTABLE.CODIGO END), 1)  CLASEORDEN,  
       CASE WHEN V_PLAN_CONTABLE.NATURALEZA = 'C' THEN V_PLAN_CONTABLE.SALDOs$mesTrabajo1$s ELSE V_PLAN_CONTABLE.SALDOs$mesTrabajo1$s * - 1 END  SALDOANT1,  
       CASE WHEN V_PLAN_CONTABLE.NATURALEZA = 'C' THEN V_PLAN_CONTABLE.NETOs$mesTrabajo$s ELSE V_PLAN_CONTABLE.NETOs$mesTrabajo$s * - 1 END  NETO1,  
       CASE WHEN V_PLAN_CONTABLE.NATURALEZA = 'C' THEN V_PLAN_CONTABLE.SALDOs$mesTrabajo$s ELSE V_PLAN_CONTABLE.SALDOs$mesTrabajo$s * - 1 END  SALDO1, 
       V_PLAN_CONTABLE.ANO,  
       V_PLAN_CONTABLE.ID      
FROM V_PLAN_CONTABLE  
WHERE V_PLAN_CONTABLE.COMPANIA = s$compania$s 
AND V_PLAN_CONTABLE.ANO = s$anoTrabajo$s  
AND ((V_PLAN_CONTABLE.CODIGO BETWEEN s$codigoInicial$s AND s$codigoFinal$s  
AND LENGTH(V_PLAN_CONTABLE.CODIGO) <= 1 )  
OR (V_PLAN_CONTABLE.CODIGO = '323001'  
OR V_PLAN_CONTABLE.CODIGO = '323002'  ) )  
AND  V_PLAN_CONTABLE.SALDOs$mesTrabajo$s NOT IN (0)  
ORDER BY  
    V_PLAN_CONTABLE.COMPANIA,  
    V_PLAN_CONTABLE.ANO,  
    V_PLAN_CONTABLE.ID
