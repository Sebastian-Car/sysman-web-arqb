MERGE INTO CONSULTAS FIN USING (SELECT '002445SubResumenBalanceBancosCOR' INFORME ,TO_CLOB(q'[SELECT 
  V_PLAN_CONTABLE.CODIGO, 
  V_PLAN_CONTABLE.NOMBRE, 
  V_PLAN_CONTABLE.SALDOs$mesAnterior$s + NVL(FRACCION.SALDOALDIA,0) SALDOI, 
  SUM(CASE WHEN TO_CHAR(DETALLE_COMPROBANTE_CNT.FECHA,'DD/MM/YYYY')='s$fecha$s' THEN DETALLE_COMPROBANTE_CNT.VALOR_DEBITO ELSE 0 END) DEBITO,  
  SUM(CASE WHEN TO_CHAR(DETALLE_COMPROBANTE_CNT.FECHA,'DD/MM/YYYY')='s$fecha$s' THEN DETALLE_COMPROBANTE_CNT.VALOR_CREDITO ELSE 0 END) CREDITO,  
  V_PLAN_CONTABLE.SALDOs$mesAnterior$s + NVL(FRACCION.SALDOALDIA,0)+SUM(CASE WHEN TO_CHAR(DETALLE_COMPROBANTE_CNT.FECHA,'DD/MM/YYYY')='s$fecha$s' THEN DETALLE_COMPROBANTE_CNT.VALOR_DEBITO ELSE 0 END) - SUM(CASE WHEN TO_CHAR(DETALLE_COMPROBANTE_CNT.FECHA,'DD/MM/YYYY')='s$fecha$s' THEN DETALLE_COMPROBANTE_CNT.VALOR_CREDITO ELSE 0 END) SALDOFINAL 
FROM V_PLAN_CONTABLE 
  LEFT JOIN DETALLE_COMPROBANTE_CNT  
    ON  V_PLAN_CONTABLE.COMPANIA        = DETALLE_COMPROBANTE_CNT.COMPANIA  
    AND V_PLAN_CONTABLE.ANO             = DETALLE_COMPROBANTE_CNT.ANO 
    AND V_PLAN_CONTABLE.CODIGO          = DETALLE_COMPROBANTE_CNT.CUENTA  
 LEFT JOIN ( 
             SELECT PLAN.COMPANIA, PLAN.ANO, PLAN.CODIGO, 
                   SUM( CASE WHEN PLAN.NATURALEZA='D'  
                      THEN DETFRACC.VALOR_DEBITO  -DETFRACC.VALOR_CREDITO 
                      ELSE DETFRACC.VALOR_CREDITO -DETFRACC.VALOR_DEBITO 
                      END 
                      ) SALDOALDIA 
             FROM DETALLE_COMPROBANTE_CNT DETFRACC INNER JOIN V_PLAN_CONTABLE PLAN 
              ON  DETFRACC.COMPANIA = PLAN.COMPANIA  
              AND DETFRACC.ANO      = PLAN.ANO  
              AND DETFRACC.CUENTA   = PLAN.CODIGO 
             WHERE PLAN.COMPANIA= s$compania$s 
               AND PLAN.ANO     = s$anio$s 
               AND PLAN.CLASECUENTA IN ('B') 
               AND PLAN.MOVIMIENTO NOT IN (0) 
               AND DETFRACC.FECHA <  TO_DATE('s$fecha$s', 'DD/MM/YYYY' ) 
               AND DETFRACC.FECHA >= TO_DATE('01/' || TO_CHAR(TO_DATE('s$fecha$s', 'DD/MM/YYYY' ),'MM') || TO_CHAR(TO_DATE('s$fecha$s', 'DD/MM/YYYY' ),'YYYY')) 
             GROUP BY  PLAN.COMPANIA, PLAN.ANO, PLAN.CODIGO 
              ) FRACCION 
    ON V_PLAN_CONTABLE.COMPANIA = FRACCION.COMPANIA             
   AND FRACCION.ANO             = V_PLAN_CONTABLE.ANO    
   AND FRACCION.CODIGO          = V_PLAN_CONTABLE.CODIGO 
WHERE V_PLAN_CONTABLE.COMPANIA= s$compania$s 
  AND V_PLAN_CONTABLE.ANO     = s$anio$s 
  AND V_PLAN_CONTABLE.CLASECUENTA IN ('B') 
  AND V_PLAN_CONTABLE.MOVIMIENTO NOT IN (0) 
GROUP BY 
  V_PLAN_CONTABLE.CODIGO, V_PLAN_CONTABLE.NOMBRE, 
  V_PLAN_CONTABLE.SALDOs$mesAnterior$s, FRACCION.SALDOALDIA
HAVING (V_PLAN_CONTABLE.SALDOs$mesAnterior$s + NVL(FRACCION.SALDOALDIA,0) +  SUM(CASE WHEN TO_CHAR(DETALLE_COMPROBANTE_CNT.FECHA,'DD/MM/YYYY')='s$fecha$s' THEN DETALLE_COMPROBANTE_CNT.VALOR_DEBITO ELSE 0 END) + SUM(CASE WHEN TO_CHAR(DETALLE_COMPROBANTE_CNT.FECHA,'DD/MM/YYYY')='s$fecha$s' THEN DETALLE_COMPROBANTE_CNT.VALOR_CREDITO ELSE 0 END))>0]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);


MERGE INTO CONSULTAS_SUB FIN USING (SELECT '002437LisBoletinDiarioTesoreriaCOR' INFORME_PADRE, '002445SubResumenBalanceBancosCOR' INFORME_HIJO, 'PR_STRSQL_SUBRESUMENBALANCEBANCOS' PARAMETRO ,NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (FIN.INFORME_PADRE = INI.INFORME_PADRE AND INI.INFORME_HIJO = FIN.INFORME_HIJO)  WHEN MATCHED THEN  UPDATE SET FIN.PARAMETRO = INI.PARAMETRO , FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME_PADRE,INFORME_HIJO, PARAMETRO,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME_PADRE,INI.INFORME_HIJO, INI.PARAMETRO,INI.CREATED_BY,SYSDATE);