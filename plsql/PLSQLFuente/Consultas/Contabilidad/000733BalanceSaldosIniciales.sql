SELECT  
   SALDOSINICIALES.COMPANIA
  ,SUBSTR(SALDOSINICIALES.CODIGO,1,1) CLASE        
  ,SALDOSINICIALES.CODIGO
  ,PLAN_CONTABLE.NOMBRE NOMCTA
  ,CASE WHEN SALDOSINICIALES.CENTRO_COSTO = '99999999999999999999' 
        THEN '' 
        ELSE SALDOSINICIALES.CENTRO_COSTO 
   END CENTRO_COSTO
  ,CASE WHEN SALDOSINICIALES.AUXILIAR = '99999999999999999999' 
        THEN '' 
        ELSE SALDOSINICIALES.AUXILIAR 
   END AUXILIAR
  ,CASE WHEN SALDOSINICIALES.TERCERO = '999999999999999999' 
        THEN '' 
        ELSE SALDOSINICIALES.TERCERO || PCK_SYSMAN_UTL.FC_DCH(SALDOSINICIALES.TERCERO) 
   END NITCOMPLETO
  ,SALDOSINICIALES.DEBITO
  ,SALDOSINICIALES.CREDITO
FROM SALDOSINICIALES
  INNER JOIN PLAN_CONTABLE 
     ON SALDOSINICIALES.COMPANIA = PLAN_CONTABLE.COMPANIA 
    AND SALDOSINICIALES.ANO      = PLAN_CONTABLE.ANO
    AND SALDOSINICIALES.CODIGO   = PLAN_CONTABLE.CODIGO
WHERE SALDOSINICIALES.COMPANIA = 's$compania$s'
  AND SALDOSINICIALES.ANO      = s$anoTrabajo$s
