SELECT BALANCE_NIIF.CLASE,
  BALANCE_NIIF.CLASEORDEN,
  BALANCE_NIIF.ANO,
  BALANCE_NIIF.ID,
  BALANCE_NIIF.ORDEN,
  BALANCE_NIIF.ORDENCUENTAS,
  BALANCE_NIIF.CODIGO,
  BALANCE_NIIF.CENTRO_COSTO,
  BALANCE_NIIF.TERCERO,
  BALANCE_NIIF.SUCURSAL,
  BALANCE_NIIF.AUXILIAR,
  BALANCE_NIIF.ORDENCUENTAS,
  BALANCE_NIIF.SALDO_1,
  BALANCE_NIIF.SALDO,
  BALANCE_NIIF.DEBITO,
  BALANCE_NIIF.CREDITO,
  BALANCE_NIIF.SALDOANTDEBITO,
  BALANCE_NIIF.SALDOANTCREDITO,
  BALANCE_NIIF.SALDONUEDEBITO,
  BALANCE_NIIF.SALDONUECREDITO,
  BALANCE_BASE.CODIGO,
  BALANCE_BASE.ORDENCUENTAS,
  BALANCE_BASE.SALDO_1,
  BALANCE_BASE.SALDO,
  BALANCE_BASE.DEBITO,
  BALANCE_BASE.CREDITO,
  BALANCE_BASE.SALDOANTDEBITO,
  BALANCE_BASE.SALDOANTCREDITO,
  BALANCE_BASE.SALDONUEDEBITO,
  BALANCE_BASE.SALDONUECREDITO,
  BALANCE_NIIF.SALDO-BALANCE_BASE.SALDO AS DIFERENCIA
FROM (
SELECT SUBSTR(PLAN_CONTABLE.CODIGO,1,1) AS CLASE,
  PLAN_CONTABLE.SALDOs$mestrabajo$s AS SALDO,
  PLAN_CONTABLE.ANO,
  PLAN_CONTABLE.SALDOSs$mestrabajoMen$s  AS SALDO_1,
  PLAN_CONTABLE.CODIGO,
  PLAN_CONTABLE.CENTRO_COSTO,
  PLAN_CONTABLE.NOMBRE,
  PLAN_CONTABLE.TERCERO,
  PLAN_CONTABLE.SUCURSAL,
  PLAN_CONTABLE.AUXILIAR,
  PLAN_CONTABLE.DEBITOs$mestrabajo$s  AS DEBITO ,
  PLAN_CONTABLE.CREDITOs$mestrabajo$s   AS CREDITO,
  PLAN_CONTABLE.DEBITOs$mestrabajoMen$s AS DEBITO_1,
  PLAN_CONTABLE.CREDITOs$mestrabajoMen$s AS CREDITO_1 ,
  PLAN_CONTABLE.NATURALEZA,
  PLAN_CONTABLE.NETOs$mestrabajoMen$s,
  CASE WHEN PLAN_CONTABLE.NATURALEZA='D' AND PLAN_CONTABLE.SALDOSs$mestrabajoMen$s >=0
      THEN PLAN_CONTABLE.SALDOSs$mestrabajoMen$s
      ELSE 0
      END - CASE WHEN PLAN_CONTABLE.NATURALEZA='C' AND PLAN_CONTABLE.SALDOSs$mestrabajoMen$s<0
      THEN -PLAN_CONTABLE.SALDOSs$mestrabajoMen$s
      ELSE 0
      END AS SALDOANTDEBITO,
  CASE WHEN PLAN_CONTABLE.NATURALEZA='C' AND PLAN_CONTABLE.SALDOSs$mestrabajoMen$s>=0
      THEN PLAN_CONTABLE.SALDOSs$mestrabajoMen$s
      ELSE 0
      END - CASE WHEN PLAN_CONTABLE.NATURALEZA='D' AND PLAN_CONTABLE.SALDOSs$mestrabajoMen$s <0
            THEN -PLAN_CONTABLE.SALDOSs$mestrabajoMen$s 
            ELSE 0
            END AS SALDOANTCREDITO,
  CASE WHEN PLAN_CONTABLE.NATURALEZA='D' AND PLAN_CONTABLE.SALDOs$mestrabajo$s >=0
      THEN PLAN_CONTABLE.SALDOs$mestrabajo$s 
      ELSE 0
      END - CASE WHEN PLAN_CONTABLE.NATURALEZA='C' AND PLAN_CONTABLE.SALDOs$mestrabajo$s <0
      THEN PLAN_CONTABLE.SALDOs$mestrabajo$s
      ELSE 0
      END AS SALDONUEDEBITO,
  CASE WHEN PLAN_CONTABLE.NATURALEZA='C' AND PLAN_CONTABLE.SALDOs$mestrabajo$s>=0 
      THEN PLAN_CONTABLE.SALDOs$mestrabajo$s
      ELSE 0
      END - CASE WHEN PLAN_CONTABLE.NATURALEZA='D' AND PLAN_CONTABLE.SALDOs$mestrabajo$s<0
          THEN PLAN_CONTABLE.SALDOs$mestrabajo$s
          ELSE 0
          END AS SALDONUECREDITO,
  PLAN_CONTABLE.ID,
  CASE WHEN TERCERO.CLASE='0'
      THEN 'z'||PLAN_CONTABLE.CODIGO
      ELSE PLAN_CONTABLE.CODIGO
  END AS ORDEN,
  SUBSTR(ORDEN,1,1) AS CLASEORDEN,
  TERCERO.NOMBRE,
  TERCERO.DIGITOVERIFICACION,
  AUXILIAR.NOMBRE,
  CENTRO_COSTO.NOMBRE,
  TERCERO.NOMBRE,
  PLAN_CONTABLE.CODIGO|| 
  CASE WHEN CENTRO_COSTO IS NULL THEN ' ' ELSE CENTRO_COSTO.NOMBRE END || 
  CASE WHEN TERCERO IS NULL THEN ' ' ELSE TERCERO.NOMBRE END || 
  CASE WHEN AUXILIAR IS NULL THEN ' ' ELSE AUXILIAR.NOMBRE END ||
  PLAN_CONTABLE.NOMBRE AS ORDENCUENTAS,
  PLAN_CONTABLE.TERCERO||TERCERO.DIGITOVERIFICACION AS NITCOMPLETO   
  FROM V_PLAN_CONTABLE 
    LEFT JOIN TERCERO 
      ON V_PLAN_CONTABLE.COMPANIA = TERCERO.COMPANIA
      AND V_PLAN_CONTABLE.TERCERO = TERCERO.NIT
      AND V_PLAN_CONTABLE.SUCURSAL = TERCERO.SUCURSAL 
        LEFT JOIN AUXILIAR 
          ON V_PLAN_CONTABLE.COMPANIA = AUXILIAR.COMPANIA
          AND V_PLAN_CONTABLE.AUXILIAR = AUXILIAR.CODIGO 
            LEFT JOIN CENTRO_COSTO 
              ON (V_PLAN_CONTABLE.COMPANIA = CENTRO_COSTO.COMPANIA) 
              AND       V_PLAN_CONTABLE.CENTRO_COSTO = CENTRO_COSTO.CODIGO 
  WHERE PLAN_CONTABLE.COMPANIA =  s$companiaNif$s
  AND PLAN_CONTABLE.ANO = s$ano$s
  AND PLAN_CONTABLE.CODIGO >= s$codigoInicial$s
  AND PLAN_CONTABLE.CODIGO <= s$codigoFinal$s
  AND SUBSTR(PLAN_CONTABLE.CODIGO,1,1)<= s$digitos$s
  AND V_PLAN_CONTABLE.AUXILIAR IS NULL
AND V_PLAN_CONTABLE.CENTRO_COSTO IS NULL
AND V_PLAN_CONTABLE.TERCERO IS NULL
AND (ROUND(V_PLAN_CONTABLE.SALDOs$mestrabajo$s  ,2) <>0
OR ROUND(V_PLAN_CONTABLE.DEBITOs$mestrabajo$s ,2) <>0 OR ROUND(V_PLAN_CONTABLE.CREDITOs$mestrabajo$s 
 ,2) <>0 OR ROUND(V_PLAN_CONTABLE.SALDOs$mestrabajoMen$s
 ,2)<>0)
ORDER BY V_PLAN_CONTABLE.ID 
) BALANCE_NIIF INNER JOIN (
SELECT SUBSTR(PLAN_CONTABLE.CODIGO,1,1) AS CLASE,
  PLAN_CONTABLE.SALDOs$mestrabajo$s AS SALDO,
  PLAN_CONTABLE.ANO,
  PLAN_CONTABLE.SALDOSs$mestrabajoMen$s  AS SALDO_1,
  PLAN_CONTABLE.CODIGO,
  PLAN_CONTABLE.CENTRO_COSTO,
  PLAN_CONTABLE.NOMBRE,
  PLAN_CONTABLE.TERCERO,
  PLAN_CONTABLE.SUCURSAL,
  PLAN_CONTABLE.AUXILIAR,
  PLAN_CONTABLE.DEBITOs$mestrabajo$s  AS DEBITO ,
  PLAN_CONTABLE.CREDITOs$mestrabajo$s   AS CREDITO,
  PLAN_CONTABLE.DEBITOs$mestrabajoMen$s AS DEBITO_1,
  PLAN_CONTABLE.CREDITOs$mestrabajoMen$s AS CREDITO_1 ,
  PLAN_CONTABLE.NATURALEZA,
  PLAN_CONTABLE.NETOs$mestrabajoMen$s,
  CASE WHEN PLAN_CONTABLE.NATURALEZA='D' AND PLAN_CONTABLE.SALDOSs$mestrabajoMen$s >=0
      THEN PLAN_CONTABLE.SALDOSs$mestrabajoMen$s
      ELSE 0
      END - CASE WHEN PLAN_CONTABLE.NATURALEZA='C' AND PLAN_CONTABLE.SALDOSs$mestrabajoMen$s<0
      THEN -PLAN_CONTABLE.SALDOSs$mestrabajoMen$s
      ELSE 0
      END AS SALDOANTDEBITO,
  CASE WHEN PLAN_CONTABLE.NATURALEZA='C' AND PLAN_CONTABLE.SALDOSs$mestrabajoMen$s>=0
      THEN PLAN_CONTABLE.SALDOSs$mestrabajoMen$s
      ELSE 0
      END - CASE WHEN PLAN_CONTABLE.NATURALEZA='D' AND PLAN_CONTABLE.SALDOSs$mestrabajoMen$s <0
            THEN -PLAN_CONTABLE.SALDOSs$mestrabajoMen$s 
            ELSE 0
            END AS SALDOANTCREDITO,
  CASE WHEN PLAN_CONTABLE.NATURALEZA='D' AND PLAN_CONTABLE.SALDOs$mestrabajo$s >=0
      THEN PLAN_CONTABLE.SALDOs$mestrabajo$s 
      ELSE 0
      END - CASE WHEN PLAN_CONTABLE.NATURALEZA='C' AND PLAN_CONTABLE.SALDOs$mestrabajo$s <0
      THEN PLAN_CONTABLE.SALDOs$mestrabajo$s
      ELSE 0
      END AS SALDONUEDEBITO,
  CASE WHEN PLAN_CONTABLE.NATURALEZA='C' AND PLAN_CONTABLE.SALDOs$mestrabajo$s>=0 
      THEN PLAN_CONTABLE.SALDOs$mestrabajo$s
      ELSE 0
      END - CASE WHEN PLAN_CONTABLE.NATURALEZA='D' AND PLAN_CONTABLE.SALDOs$mestrabajo$s<0
          THEN PLAN_CONTABLE.SALDOs$mestrabajo$s
          ELSE 0
          END AS SALDONUECREDITO,
  PLAN_CONTABLE.ID,
  CASE WHEN TERCERO.CLASE='0'
      THEN 'z'||PLAN_CONTABLE.CODIGO
      ELSE PLAN_CONTABLE.CODIGO
  END AS ORDEN,
  SUBSTR(ORDEN,1,1) AS CLASEORDEN,
  TERCERO.NOMBRE,
  TERCERO.DIGITOVERIFICACION,
  AUXILIAR.NOMBRE,
  CENTRO_COSTO.NOMBRE,
  TERCERO.NOMBRE,
  PLAN_CONTABLE.CODIGO|| 
  CASE WHEN CENTRO_COSTO IS NULL THEN ' ' ELSE CENTRO_COSTO.NOMBRE END || 
  CASE WHEN TERCERO IS NULL THEN ' ' ELSE TERCERO.NOMBRE END || 
  CASE WHEN AUXILIAR IS NULL THEN ' ' ELSE AUXILIAR.NOMBRE END ||
  PLAN_CONTABLE.NOMBRE AS ORDENCUENTAS,
  PLAN_CONTABLE.TERCERO||TERCERO.DIGITOVERIFICACION AS NITCOMPLETO,
  NVL (V_PLAN_CONTABLE.CODIGO_NIIF,V_PLAN_CONTABLE.CODIGO) AS CODIGO_NIIF
  FROM V_PLAN_CONTABLE 
    LEFT JOIN TERCERO 
      ON V_PLAN_CONTABLE.COMPANIA = TERCERO.COMPANIA
      AND V_PLAN_CONTABLE.TERCERO = TERCERO.NIT
      AND V_PLAN_CONTABLE.SUCURSAL = TERCERO.SUCURSAL 
        LEFT JOIN AUXILIAR 
          ON V_PLAN_CONTABLE.COMPANIA = AUXILIAR.COMPANIA
          AND V_PLAN_CONTABLE.AUXILIAR = AUXILIAR.CODIGO 
            LEFT JOIN CENTRO_COSTO 
              ON V_PLAN_CONTABLE.CENTRO_COSTO = CENTRO_COSTO.CODIGO 
              AND (V_PLAN_CONTABLE.COMPANIA = CENTRO_COSTO.COMPANIA)       
  WHERE PLAN_CONTABLE.COMPANIA =  s$compania$s
  AND PLAN_CONTABLE.ANO = s$ano$s
  AND PLAN_CONTABLE.CODIGO >= s$codigoInicial$s
  AND PLAN_CONTABLE.CODIGO <= s$codigoFinal$s
  AND SUBSTR(PLAN_CONTABLE.CODIGO,1,1)<= s$digitos$s
  AND V_PLAN_CONTABLE.AUXILIAR IS NULL
AND V_PLAN_CONTABLE.CENTRO_COSTO IS NULL
AND V_PLAN_CONTABLE.TERCERO IS NULL
AND (ROUND(V_PLAN_CONTABLE.SALDOs$mestrabajo$s  ,2) <>0
OR ROUND(V_PLAN_CONTABLE.DEBITOs$mestrabajo$s ,2) <>0 OR ROUND(V_PLAN_CONTABLE.CREDITOs$mestrabajo$s 
 ,2) <>0 OR ROUND(V_PLAN_CONTABLE.SALDOs$mestrabajoMen$s
 ,2)<>0)
ORDER BY V_PLAN_CONTABLE.ID 
) BALANCE_BASE
ON BALANCE_NIIF.ANO     = BALANCE_BASE.ANO
AND BALANCE_NIIF.CODIGO = BALANCE_BASE.CODIGO_NIIF
ORDER BY BALANCE_NIIF.CODIGO
