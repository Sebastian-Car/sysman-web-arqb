SELECT      HEADERCIERRE.CUENTAACERRAR,
             HEADERCIERRE.CONTRACUENTA,
             NVL(HEADERCIERRE.CONDICION,' ') CONDICION,
             HEADERCIERRE.ANO,
             V_PLAN_CONTABLE.NOMBRE,
             PV.NOMBRE NOMBRECONTRACUENTA
  FROM       HEADERCIERRE
  INNER JOIN V_PLAN_CONTABLE  ON (V_PLAN_CONTABLE.COMPANIA = HEADERCIERRE.COMPANIA
                        AND   V_PLAN_CONTABLE.ID = HEADERCIERRE.CUENTAACERRAR
                        AND   V_PLAN_CONTABLE.ANO = HEADERCIERRE.ANO)
  INNER JOIN V_PLAN_CONTABLE PV  ON (PV.COMPANIA = HEADERCIERRE.COMPANIA
                        AND   PV.ID = HEADERCIERRE.CONTRACUENTA
                        AND   PV.ANO = HEADERCIERRE.ANO)                        
  WHERE      HEADERCIERRE.COMPANIA = s$compania$s
    AND      HEADERCIERRE.ANO      =  s$anio$s
