SELECT  INVERSIONRV.CODIGO,
        INVERSIONRV.FONDO,
        FUENTE_RECURSOS.NOMBRE ORIGENRECURSOS,
        INVERSIONRV.ENTIDADEMISORA,
        INVERSIONRV.VI_NUMERO,
        INVERSIONRV.VI_VALORUNITARIO,
        INVERSIONRV.VI_VALORNOMGLOBAL,
        INVERSIONRV.RF_NUMERO,
        INVERSIONRV.RF_VALORUNITARIO,
        INVERSIONRV.RF_VALORNOMGLOBAL,
        INVERSIONRV.FECHAADQUISICION,
        INVERSIONRV.FECHAREPORREND,
        DECODE (INVERSIONRV.SITUACIONFINAL, 'C', 'Continuación' , 'L' , 'Liquidación')SITUACIONFINAL
FROM INVERSIONRV
  LEFT JOIN FUENTE_RECURSOS
    ON INVERSIONRV.COMPANIA = FUENTE_RECURSOS.COMPANIA
    AND INVERSIONRV.ANO = FUENTE_RECURSOS.ANO
    AND INVERSIONRV.ORIGENRECURSOS = FUENTE_RECURSOS.CODIGO
WHERE INVERSIONRV.COMPANIA = s$compania$s
  AND FECHAADQUISICION  BETWEEN s$fechaInicial$s AND s$fechaFinal$s
