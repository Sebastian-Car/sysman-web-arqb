SELECT  DISTINCT 
         AUXILIAR.COMPANIA,
         AUXILIAR.ANO,
         AUXILIAR.CODIGO,  
         AUXILIAR.NOMBRE,  
         DECODE(AUXILIAR.MOVIMIENTO,'-1','SI','NO') MOVIMIENTO, 
         NVL(AUXILIAR.INVENTARIO,' ') INVENTARIO 
 FROM    AUXILIAR 
 WHERE   AUXILIAR.COMPANIA = s$compania$s
