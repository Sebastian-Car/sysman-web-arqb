MERGE INTO CONSULTAS FIN USING (SELECT '002593SubCOM_CREMIL' INFORME ,TO_CLOB(q'[SELECT  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE  NUMCOMP,
        DETALLE_COMPROBANTE_PPTAL.CUENTA,
        PLAN_PRESUPUESTAL.NOMBRE,
        NVL(DETALLE_COMPROBANTE_PPTAL_1.CMPTE_AFECTADO,0) NUMDISP, 
        DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO NUMRESERVA,
        SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA ='C' 
                 THEN DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO - DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO
                 ELSE DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO
            END) VALOR, 
            SUM(DETALLE_COMPROBANTE_PPTAL_1.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL_1.VALOR_CREDITO - (DETALLE_COMPROBANTE_PPTAL_1.DEBITO_AFECTADO     - DETALLE_COMPROBANTE_PPTAL_1.CREDITO_AFECTADO) + (DETALLE_COMPROBANTE_PPTAL_1.MODIFICACION_DEBITO - DETALLE_COMPROBANTE_PPTAL_1.MODIFICACION_CREDITO)) SALDORES 
        FROM DETALLE_COMPROBANTE_PPTAL
  INNER JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA           = PLAN_PRESUPUESTAL.COMPANIA 
    AND DETALLE_COMPROBANTE_PPTAL.ANO               = PLAN_PRESUPUESTAL.ANO
    AND DETALLE_COMPROBANTE_PPTAL.ID                 = PLAN_PRESUPUESTAL.ID
  INNER JOIN AUXILIAR
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA   = AUXILIAR.COMPANIA 
    AND DETALLE_COMPROBANTE_PPTAL.ANO       = AUXILIAR.ANO  
    AND DETALLE_COMPROBANTE_PPTAL.AUXILIAR  = AUXILIAR.CODIGO
  LEFT JOIN DETALLE_COMPROBANTE_PPTAL  DETALLE_COMPROBANTE_PPTAL_1 
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA        = DETALLE_COMPROBANTE_PPTAL_1.COMPANIA
   AND DETALLE_COMPROBANTE_PPTAL.ANO             = DETALLE_COMPROBANTE_PPTAL_1.ANO
   AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT = DETALLE_COMPROBANTE_PPTAL_1.TIPO_CPTE
   AND DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO  = DETALLE_COMPROBANTE_PPTAL_1.COMPROBANTE 
   AND DETALLE_COMPROBANTE_PPTAL.CONSECUTIVOPPTO = DETALLE_COMPROBANTE_PPTAL_1.CONSECUTIVO      
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA  = $P{PR_COMP}  
  AND DETALLE_COMPROBANTE_PPTAL.ANO       = $P{PR_ANOCOMP}
  AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = $P{PR_TIPOCOMP}
  AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = $P{PR_NUMCOMP} 
GROUP BY 
  DETALLE_COMPROBANTE_PPTAL.COMPANIA,
  DETALLE_COMPROBANTE_PPTAL.ANO,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
  PLAN_PRESUPUESTAL.NOMBRE,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT,
  DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO,
  DETALLE_COMPROBANTE_PPTAL.CUENTA,
  DETALLE_COMPROBANTE_PPTAL_1.CMPTE_AFECTADO]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, 'MPEREZ' CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);

