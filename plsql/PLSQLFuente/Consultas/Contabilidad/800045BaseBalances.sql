  FROM PLAN_CONTABLE 
    INNER JOIN SALDO_AUX_CONTABLE SALDO
      ON PLAN_CONTABLE.COMPANIA = SALDO.COMPANIA 
     AND PLAN_CONTABLE.ANO = SALDO.ANO 
     AND PLAN_CONTABLE.CODIGO = SALDO.CODIGO
    INNER JOIN TERCERO 
      ON SALDO.COMPANIA = TERCERO.COMPANIA 
     AND SALDO.TERCERO = TERCERO.NIT 
     AND SALDO.SUCURSAL = TERCERO.SUCURSAL
    INNER JOIN CENTRO_COSTO 
      ON SALDO.COMPANIA = CENTRO_COSTO.COMPANIA 
     AND SALDO.ANO = CENTRO_COSTO.ANO 
     AND SALDO.CENTRO_COSTO = CENTRO_COSTO.CODIGO
    INNER JOIN AUXILIAR 
      ON SALDO.COMPANIA = AUXILIAR.COMPANIA 
     AND SALDO.ANO = AUXILIAR.ANO 
     AND SALDO.AUXILIAR = AUXILIAR.CODIGO 
    INNER JOIN FUENTE_RECURSOS 
      ON SALDO.COMPANIA = FUENTE_RECURSOS.COMPANIA 
     AND SALDO.ANO = FUENTE_RECURSOS.ANO 
     AND SALDO.FUENTE_RECURSO = FUENTE_RECURSOS.CODIGO 
    INNER JOIN REFERENCIA 
      ON SALDO.COMPANIA = REFERENCIA.COMPANIA 
     AND SALDO.ANO = REFERENCIA.ANO 
     AND SALDO.REFERENCIA = REFERENCIA.CODIGO
WHERE PLAN_CONTABLE.COMPANIA = s$compania$s
  AND PLAN_CONTABLE.ANO = s$anio$s
  AND PLAN_CONTABLE.CODIGO BETWEEN 's$codigoInicial$s'  AND 's$codigoFinal$s'           
  AND LENGTH(PLAN_CONTABLE.codigo)<= s$digitos$s 
      s$auxiliar$s 
      s$centroCosto$s 
      s$tercero$s 
      s$saldoCero$s
GROUP BY  PLAN_CONTABLE.COMPANIA, 
          PLAN_CONTABLE.ANO, 
          PLAN_CONTABLE.CODIGO, 
          PLAN_CONTABLE.NOMBRE,
          CASE WHEN 1=s$manTer$s THEN SALDO.TERCERO ELSE NULL END,
          CASE WHEN 1=s$manAux$s THEN SALDO.AUXILIAR ELSE NULL END, 
          CASE WHEN 1=s$manCen$s THEN SALDO.CENTRO_COSTO ELSE NULL END,
          CASE WHEN 1=s$manTer$s THEN TERCERO.NOMBRE ELSE NULL END,
          CASE WHEN 1=s$manAux$s THEN AUXILIAR.NOMBRE ELSE NULL END, 
          CASE WHEN 1=s$manCen$s THEN CENTRO_COSTO.NOMBRE ELSE NULL END
