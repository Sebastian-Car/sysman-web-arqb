        CASE WHEN 1=s$manAux$s THEN SALDO.AUXILIAR ELSE NULL END AUXILIAR, 
        CASE WHEN 1=s$manCen$s THEN SALDO.CENTRO_COSTO ELSE NULL END CENTRO_COSTO,
