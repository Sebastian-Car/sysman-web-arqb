SELECT
      V_PLAN_CONTABLE.COMPANIA, 
      V_PLAN_CONTABLE.CODIGO BANCO, 
      V_PLAN_CONTABLE.NOMBRE NOMBREBANCO, 
      V_PLAN_CONTABLE.CLASECUENTA
 FROM V_PLAN_CONTABLE LEFT JOIN COMPANIA ON V_PLAN_CONTABLE.COMPANIA = COMPANIA.CODIGO
WHERE V_PLAN_CONTABLE.COMPANIA    = s$compania$s 
  AND V_PLAN_CONTABLE.CLASECUENTA = 'B'
  AND V_PLAN_CONTABLE.ANO         = PCK_SYSMAN_UTL.FC_ANIO(SYSDATE)
ORDER BY V_PLAN_CONTABLE.CODIGO
