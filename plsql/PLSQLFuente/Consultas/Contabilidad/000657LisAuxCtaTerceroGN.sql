SELECT SUM(V_DETALLE_AUXILIAR_CNT.VALOR_DEBITO)  AS SUMADEBITO,
       SUM(V_DETALLE_AUXILIAR_CNT.VALOR_CREDITO) AS SUMACREDITO,
       SUM(V_DETALLE_AUXILIAR_CNT.BASE_GRAVABLE) AS SUMABASE,
       V_DETALLE_AUXILIAR_CNT.COMPANIA,
       V_DETALLE_AUXILIAR_CNT.ANO, 
       V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA, 
       V_DETALLE_AUXILIAR_CNT.TERCERO,
       V_DETALLE_AUXILIAR_CNT.SUCURSAL,
       V_DETALLE_AUXILIAR_CNT.TERCERO_NOM TERCERONOMBRE,
       V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE,
       V_DETALLE_AUXILIAR_CNT.DIGITOVERIFICACION, 
       V_DETALLE_AUXILIAR_CNT.DIRECCION,
       V_DETALLE_AUXILIAR_CNT.DEPARTAMENTO, 
       V_DETALLE_AUXILIAR_CNT.CIUDAD, 
       V_DETALLE_AUXILIAR_CNT.PAIS,
       CIUDAD.NOMBRE NOMBRECIUDAD,
       DEPARTAMENTO.NOMBRE NOMBREDEPARTAMENTO 
FROM  V_DETALLE_AUXILIAR_CNT 
  INNER JOIN DEPARTAMENTO 
    ON  V_DETALLE_AUXILIAR_CNT.PAIS        = DEPARTAMENTO.PAIS 
    AND V_DETALLE_AUXILIAR_CNT.DEPARTAMENTO = DEPARTAMENTO.CODIGO  
  INNER JOIN CIUDAD 
    ON  V_DETALLE_AUXILIAR_CNT.PAIS         = CIUDAD.PAIS 
    AND V_DETALLE_AUXILIAR_CNT.DEPARTAMENTO = CIUDAD.DEPARTAMENTO 
    AND V_DETALLE_AUXILIAR_CNT.CIUDAD       = CIUDAD.CODIGO 
WHERE V_DETALLE_AUXILIAR_CNT.COMPANIA = s$compania$s 
  AND V_DETALLE_AUXILIAR_CNT.ANO      = s$anoTrabajo$s
  AND V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA BETWEEN 's$codigoInicial$s'
                                               AND 's$codigoFinal$s'
  AND V_DETALLE_AUXILIAR_CNT.FECHA BETWEEN s$fechaInicial$s
                                       AND s$fechaFinal$s
  AND V_DETALLE_AUXILIAR_CNT.TERCERO_NOM  BETWEEN 's$terceroInicial$s'
                                               AND 's$terceroFinal$s'
  AND V_DETALLE_AUXILIAR_CNT.TIPO_CPTE BETWEEN 's$tipoInicial$s'
                                           AND 's$tipoFinal$s'
  AND V_DETALLE_AUXILIAR_CNT.SUCURSAL BETWEEN 's$sucursalInicial$s'
                                          AND 's$sucursalFinal$s'  
GROUP BY V_DETALLE_AUXILIAR_CNT.COMPANIA, 
         V_DETALLE_AUXILIAR_CNT.ANO, 
         V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA, 
         V_DETALLE_AUXILIAR_CNT.TERCERO, 
         V_DETALLE_AUXILIAR_CNT.SUCURSAL, 
         V_DETALLE_AUXILIAR_CNT.TERCERO_NOM, 
 
         V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE, 
         V_DETALLE_AUXILIAR_CNT.DIGITOVERIFICACION, 
         V_DETALLE_AUXILIAR_CNT.DIRECCION, 
         V_DETALLE_AUXILIAR_CNT.DEPARTAMENTO, 
         V_DETALLE_AUXILIAR_CNT.CIUDAD, 
         V_DETALLE_AUXILIAR_CNT.PAIS, 
         CIUDAD.NOMBRE, 
         DEPARTAMENTO.NOMBRE
