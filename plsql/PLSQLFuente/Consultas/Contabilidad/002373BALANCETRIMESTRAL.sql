MERGE INTO CONSULTAS FIN USING (SELECT '002373BALANCETRIMESTRAL' INFORME ,TO_CLOB(q'[SELECT  
SUBSTR(V_PLAN_CONTABLE.CODIGO,1,1) AS CLASE,  
V_PLAN_CONTABLE.COMPANIA,  
V_PLAN_CONTABLE.CODIGO,  
V_PLAN_CONTABLE.CENTRO_COSTO,  
V_PLAN_CONTABLE.NOMBRE AS NOMBRE,  
V_PLAN_CONTABLE.AUXILIAR,  
SUBSTR((CASE WHEN (SUBSTR(V_PLAN_CONTABLE.CODIGO,1,1)='0') THEN 'z' || V_PLAN_CONTABLE.CODIGO ELSE V_PLAN_CONTABLE.CODIGO END),1,1) AS CLASEORDEN,   
V_PLAN_CONTABLE.TERCERO || TERCERO.DIGITOVERIFICACION AS NITCOMPLETO,
V_PLAN_CONTABLE.Saldos$mes$s - 1  as SaldoInicial,
V_PLAN_CONTABLE.Netos$mesInicial$s  MESINICIAL,
V_PLAN_CONTABLE.Netos$mesIntermedio$s  MESINTERMEDIO,
V_PLAN_CONTABLE.Netos$mesFinal$s  MESFINAL,
V_PLAN_CONTABLE.Saldos$mesInicial$s  AS SaldoFinal
FROM V_PLAN_CONTABLE 
LEFT JOIN TERCERO ON V_PLAN_CONTABLE.COMPANIA = TERCERO.COMPANIA  
  AND V_PLAN_CONTABLE.TERCERO = TERCERO.NIT  
  AND V_PLAN_CONTABLE.SUCURSAL = TERCERO.SUCURSAL 
LEFT JOIN AUXILIAR ON V_PLAN_CONTABLE.COMPANIA = AUXILIAR.COMPANIA 
  AND V_PLAN_CONTABLE.ANO      = AUXILIAR.ANO   
  AND V_PLAN_CONTABLE.AUXILIAR = AUXILIAR.CODIGO 
LEFT JOIN CENTRO_COSTO ON V_PLAN_CONTABLE.CENTRO_COSTO = CENTRO_COSTO.CODIGO 
  AND  V_PLAN_CONTABLE.ANO      = CENTRO_COSTO.ANO 
  AND V_PLAN_CONTABLE.COMPANIA = CENTRO_COSTO.COMPANIA 
LEFT JOIN REFERENCIA ON V_PLAN_CONTABLE.REFERENCIA = REFERENCIA.CODIGO 
  AND  V_PLAN_CONTABLE.ANO      = REFERENCIA.ANO 
  AND V_PLAN_CONTABLE.COMPANIA = REFERENCIA.COMPANIA 
WHERE V_PLAN_CONTABLE.COMPANIA = s$compania$s   
AND V_PLAN_CONTABLE.ANO =   s$anio$s 
AND V_PLAN_CONTABLE.CODIGO >= 's$codigoInicial$s' 
AND V_PLAN_CONTABLE.CODIGO <= 's$codigoFinal$s'  
AND LENGTH(V_PLAN_CONTABLE.CODIGO)<= s$digitos$s  
ORDER BY  V_PLAN_CONTABLE.ID]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, 'mrosero' CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);