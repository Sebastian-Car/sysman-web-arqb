MERGE INTO CONSULTAS FIN USING (SELECT '000784RelRetencionesXTerceroSog' INFORME ,TO_CLOB(q'[SELECT  
    DETALLE_COMPROBANTE_CNT.COMPANIA COMPANIA,  
    DETALLE_COMPROBANTE_CNT.ANO ANO,  
    DETALLE_COMPROBANTE_CNT.CUENTA,  
    PLAN_CONTABLE.NOMBRE NOMBRECUENTA,  
    DETALLE_COMPROBANTE_CNT.TERCERO TERCERO,  
    TERCERO.NOMBRE NOMBRETERCERO,  
    TERCERO.DIRECCION,  
    TERCERO.TELEFONOS TELEFONO,  
    CIUDAD.NOMBRE CIUDAD,  
    SUM(DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE) BASERETENCION,  
    PCK_SYSMAN_UTL.FC_ROUND(  
        (SUM(DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE) * NVL(RETENCIONES.PCT_APLICAR, 0))/100, -3
    ) VALORRETENCION, 
    
    PLAN_CONTABLE.CLASECUENTA, 
    NVL(RETENCIONES.PCT_APLICAR, 0) PORCRETENCION, 
    TERCERO.DIRECCION || ' ' || CIUDAD.NOMBRE DIRECCIONYCIUDAD 
FROM 
    DETALLE_COMPROBANTE_CNT  
    LEFT JOIN PLAN_CONTABLE  
        ON DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA  
        AND DETALLE_COMPROBANTE_CNT.ANO = PLAN_CONTABLE.ANO  
        AND DETALLE_COMPROBANTE_CNT.CUENTA = PLAN_CONTABLE.CODIGO  
    LEFT JOIN TERCERO  
        ON DETALLE_COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA  
        AND DETALLE_COMPROBANTE_CNT.TERCERO = TERCERO.NIT  
        AND DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL  
    LEFT JOIN CIUDAD  
        ON TERCERO.PAIS = CIUDAD.PAIS  
        AND TERCERO.DEPARTAMENTO = CIUDAD.DEPARTAMENTO  
        AND TERCERO.CIUDAD = CIUDAD.CODIGO  
    LEFT JOIN TIPO_COMPROBANTE  
        ON DETALLE_COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA   
        AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO       
    INNER JOIN COMPROBANTE_CNT
        ON DETALLE_COMPROBANTE_CNT.COMPANIA = COMPROBANTE_CNT.COMPANIA  
        AND DETALLE_COMPROBANTE_CNT.ANO = COMPROBANTE_CNT.ANO
        AND DETALLE_COMPROBANTE_CNT.COMPROBANTE = COMPROBANTE_CNT.NUMERO
        AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = COMPROBANTE_CNT.TIPO   
    LEFT JOIN COMPROBANTE_CNTRETENCION  
        ON  COMPROBANTE_CNT.COMPANIA = COMPROBANTE_CNTRETENCION.COMPANIA  
        AND COMPROBANTE_CNT.ANO = COMPROBANTE_CNTRETENCION.ANO
        AND COMPROBANTE_CNT.NUMERO = COMPROBANTE_CNTRETENCION.NUMERO
        AND COMPROBANTE_CNT.TIPO = COMPROBANTE_CNTRETENCION.TIPO    
    LEFT JOIN RETENCIONES  
        ON COMPROBANTE_CNTRETENCION.COMPANIA = RETENCIONES.COMPANIA  
        AND COMPROBANTE_CNTRETENCION.ANO = RETENCIONES.ANO  
        AND COMPROBANTE_CNTRETENCION.CODIGORETENCION = RETENCIONES.CODIGO
        AND COMPROBANTE_CNTRETENCION.TIPORETENCION = RETENCIONES.TIPO
        AND DETALLE_COMPROBANTE_CNT.CUENTA = RETENCIONES.CUENTA_CREDITO
    
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA = s$compania$s 
  AND DETALLE_COMPROBANTE_CNT.FECHA BETWEEN s$fechaInicial$s  
                                    AND s$fechaFinal$s 
  AND DETALLE_COMPROBANTE_CNT.CUENTA BETWEEN 's$cuentaInicial$s'  
                                     AND 's$cuentaFinal$s' 
  AND DETALLE_COMPROBANTE_CNT.TERCERO BETWEEN 's$terceroInicial$s' 
                                     AND 's$terceroFinal$s'
  AND PLAN_CONTABLE.CLASECUENTA IN ('I')  
GROUP BY 
    DETALLE_COMPROBANTE_CNT.COMPANIA,  
    DETALLE_COMPROBANTE_CNT.ANO,  
    ]') || TO_CLOB(q'[DETALLE_COMPROBANTE_CNT.CUENTA,  
    PLAN_CONTABLE.NOMBRE,  
    DETALLE_COMPROBANTE_CNT.TERCERO,  
    TERCERO.NOMBRE,  
    TERCERO.DIRECCION,  
    TERCERO.TELEFONOS,  
    CIUDAD.NOMBRE,  
    PLAN_CONTABLE.CLASECUENTA,  
    TERCERO.DIRECCION || ' ' || CIUDAD.NOMBRE,  
    RETENCIONES.PCT_APLICAR
    HAVING PCK_SYSMAN_UTL.FC_ROUND(  
        (SUM(DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE) * NVL(RETENCIONES.PCT_APLICAR, 0))/100, -3
    )>0]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);