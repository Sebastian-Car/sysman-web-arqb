MERGE INTO CONSULTAS FIN USING (SELECT '002010SubinformeLisResultadosAux' INFORME ,TO_CLOB(q'[SELECT
COMPANIA,
CODIGO, 
PLAN_CONTABLE_NOMBRE,  
(SALDOANT)SALDOANT,  
(NETO) NETO,  
(SALDO) SALDO  
FROM (
SELECT BASE_BALANCE.COMPANIA, 
  --     s$mesTrabajo$s AS MES,  
       BASE_BALANCE.CODIGO ID, 
      BASE_BALANCE.ANO, 
    SUBSTR(BASE_BALANCE.CODIGO,0,1) CODIGO, 
       PLAN_CONTABLE.NOMBRE AS PLAN_CONTABLE_NOMBRE,  
       BASE_BALANCE.SALDO AS SALDOANT,  
       BASE_BALANCE.NETO AS NETO,  
       BASE_BALANCE.SALDO AS SALDO, 
       BASE_BALANCE.NATURALEZA, 
       LPAD(ORDEN,1) AS CLASEORDEN, 
       (CASE WHEN BASE_BALANCE.NATURALEZA='C' THEN BASE_BALANCE.SALDO ELSE (BASE_BALANCE.SALDO*-1) END) SALDOANT1, 
       (CASE WHEN BASE_BALANCE.NATURALEZA='C' THEN BASE_BALANCE.NETO ELSE (BASE_BALANCE.NETO*-1) END) NETO1, 
       (CASE WHEN BASE_BALANCE.NATURALEZA='C' THEN BASE_BALANCE.SALDO ELSE (BASE_BALANCE.SALDO*-1) END) SALDO1 
 FROM (s$baseBalance$s) BASE_BALANCE
INNER JOIN PLAN_CONTABLE 
ON BASE_BALANCE.COMPANIA=PLAN_CONTABLE.COMPANIA
AND BASE_BALANCE.ANO=PLAN_CONTABLE.ANO
AND SUBSTR(BASE_BALANCE.CODIGO,0,1)=PLAN_CONTABLE.CODIGO 
WHERE BASE_BALANCE.SALDO NOT IN (0) 

AND LENGTH(BASE_BALANCE.CODIGO)<=1 


 ORDER BY BASE_BALANCE.COMPANIA, BASE_BALANCE.ANO, BASE_BALANCE.CODIGO)]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
 
