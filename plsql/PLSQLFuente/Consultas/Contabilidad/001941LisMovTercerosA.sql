SELECT
    D_MOVIMIENTO.COMPANIA,
    D_MOVIMIENTO.TIPOMOVIMIENTO,
    D_MOVIMIENTO.MOVIMIENTO,
    D_MOVIMIENTO.ELEMENTO,
    D_MOVIMIENTO.SERIE,
    D_MOVIMIENTO.CANTIDAD,
    D_MOVIMIENTO.VALORUNITARIO,
    D_MOVIMIENTO.VALORTOTAL,
    D_MOVIMIENTO.FECHA,
    D_MOVIMIENTO.HORA,
    MOVIMIENTO.TERCERO,
    MOVIMIENTO.SUCURSAL,
    MOVIMIENTO.DESCRIPCION,
    PCK_SYSMAN_UTL.FC_MES(D_MOVIMIENTO.FECHA)   MOVIMIENTO_MES,
    PCK_SYSMAN_UTL.FC_ANIO(D_MOVIMIENTO.FECHA)  MOVIMIENTO_ANO,
    TERCERO.NOMBRE,
    INVENTARIO.NOMBRELARGO
FROM  D_MOVIMIENTO  
    LEFT JOIN  MOVIMIENTO
        ON D_MOVIMIENTO.COMPANIA = MOVIMIENTO.COMPANIA
        AND D_MOVIMIENTO.MOVIMIENTO = MOVIMIENTO.NUMERO 
        AND D_MOVIMIENTO.TIPOMOVIMIENTO = MOVIMIENTO.TIPOMOVIMIENTO 
    INNER JOIN INVENTARIO 
        ON INVENTARIO.COMPANIA = D_MOVIMIENTO.COMPANIA
        AND INVENTARIO.CODIGOELEMENTO = D_MOVIMIENTO.ELEMENTO 
    LEFT JOIN TERCERO 
        ON  TERCERO.COMPANIA = MOVIMIENTO.COMPANIA 
        AND TERCERO.SUCURSAL =  MOVIMIENTO.SUCURSAL  
        AND  TERCERO.NIT = MOVIMIENTO.TERCERO
WHERE D_MOVIMIENTO.COMPANIA = s$compania$s
    AND PCK_SYSMAN_UTL.FC_ANIO(D_MOVIMIENTO.FECHA) = s$ano$s
    AND   PCK_SYSMAN_UTL.FC_MES(D_MOVIMIENTO.FECHA) BETWEEN s$fechaInicial$s AND s$fechaFinal$s
    AND    MOVIMIENTO.TERCERO = 's$terceroNit$s'
    AND  MOVIMIENTO.SUCURSAL = 's$terceroSucursal$s'
ORDER BY
    D_MOVIMIENTO.COMPANIA,
    D_MOVIMIENTO.TIPOMOVIMIENTO,
    D_MOVIMIENTO.MOVIMIENTO,
    D_MOVIMIENTO.CODIGO
