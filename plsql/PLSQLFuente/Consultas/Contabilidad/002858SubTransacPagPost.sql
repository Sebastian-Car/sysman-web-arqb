        COMPROBANTE,T.NOMBRE TERCERONOMBRE
FROM    DETALLE_COMPROBANTE_CNT D
        INNER JOIN TERCERO T
        ON  (D.SUCURSAL = T.SUCURSAL)
        AND (D.TERCERO = T.NIT)
        AND (D.COMPANIA = T.COMPANIA)
        INNER JOIN TIPO_COMPROBANTE TC
