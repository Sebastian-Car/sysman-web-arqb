SELECT V_PLAN_CONTABLE.COMPANIA,
  V_PLAN_CONTABLE.ANO,
  V_PLAN_CONTABLE.ID,
  SUBSTR(V_PLAN_CONTABLE.CODIGO,0,1) CLASE,
  V_PLAN_CONTABLE.CODIGO,
  V_PLAN_CONTABLE.NATURALEZA,
     SUM(CASE WHEN 0  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.DEBITO0  ELSE 0 END
       + CASE WHEN 1  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.DEBITO1  ELSE 0 END
       + CASE WHEN 2  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.DEBITO2  ELSE 0 END
       + CASE WHEN 3  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.DEBITO3  ELSE 0 END
       + CASE WHEN 4  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.DEBITO4  ELSE 0 END
       + CASE WHEN 5  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.DEBITO5  ELSE 0 END
       + CASE WHEN 6  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.DEBITO6  ELSE 0 END
       + CASE WHEN 7  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.DEBITO7  ELSE 0 END
       + CASE WHEN 8  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.DEBITO8  ELSE 0 END
       + CASE WHEN 9  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.DEBITO9  ELSE 0 END
       + CASE WHEN 10 BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.DEBITO10 ELSE 0 END
       + CASE WHEN 11 BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.DEBITO11 ELSE 0 END
       + CASE WHEN 12 BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.DEBITO12 ELSE 0 END
       + CASE WHEN 13 BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.DEBITO13 ELSE 0 END
       ) SUMADEDEBITO,
     SUM(CASE WHEN 0  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.CREDITO0  ELSE 0 END
       + CASE WHEN 1  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.CREDITO1  ELSE 0 END
       + CASE WHEN 2  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.CREDITO2  ELSE 0 END
       + CASE WHEN 3  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.CREDITO3  ELSE 0 END
       + CASE WHEN 4  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.CREDITO4  ELSE 0 END
       + CASE WHEN 5  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.CREDITO5  ELSE 0 END
       + CASE WHEN 6  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.CREDITO6  ELSE 0 END
       + CASE WHEN 7  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.CREDITO7  ELSE 0 END
       + CASE WHEN 8  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.CREDITO8  ELSE 0 END
       + CASE WHEN 9  BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.CREDITO9  ELSE 0 END
       + CASE WHEN 10 BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.CREDITO10 ELSE 0 END
       + CASE WHEN 11 BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.CREDITO11 ELSE 0 END
       + CASE WHEN 12 BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.CREDITO12 ELSE 0 END
       + CASE WHEN 13 BETWEEN s$mesinicial$s AND s$mesfinal$s   THEN V_PLAN_CONTABLE.CREDITO13 ELSE 0 END
       ) SUMADECREDITO,
  CASE WHEN SUBSTR(V_PLAN_CONTABLE.CODIGO,0,1) IN ('0')
       THEN 'Z' || ID
       ELSE ID 
  END ORDEN,
  SUBSTR(ORDEN,0,1) CLASEORDEN,
  V_PLAN_CONTABLE.CENTRO_COSTO,
  V_PLAN_CONTABLE.NOMBRE,
  V_PLAN_CONTABLE.TERCERO,
  V_PLAN_CONTABLE.SUCURSAL,
  V_PLAN_CONTABLE.AUXILIAR,
  V_PLAN_CONTABLE.SALDOs$mesfinal$s  AS NUEVOSALDO,
  V_PLAN_CONTABLE.SALDOs$mesI$s     AS SALDOBASE,
  V_PLAN_CONTABLE.AJUSTEs$mesfinal$s AS AJUSTE,
  V_PLAN_CONTABLE.SALDOs$mesfinal$s  AS CONTABILIDAD,
  CASE WHEN V_PLAN_CONTABLE.CLASECUENTA = 'B'
       THEN V_PLAN_CONTABLE.NOMBRE
       ELSE ' '
  END BANCO
FROM V_PLAN_CONTABLE
WHERE V_PLAN_CONTABLE.COMPANIA  = s$compania$s
AND V_PLAN_CONTABLE.ANO         = s$anio$s
AND V_PLAN_CONTABLE.CODIGO BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
AND V_PLAN_CONTABLE.MOVIMIENTO   NOT IN (0)
AND LENGTH(V_PLAN_CONTABLE.CODIGO) <= s$numeroDigitos$s
GROUP BY  V_PLAN_CONTABLE.COMPANIA,
  V_PLAN_CONTABLE.ANO,
  V_PLAN_CONTABLE.ID,
  SUBSTR(V_PLAN_CONTABLE.CODIGO,0,1) ,
  V_PLAN_CONTABLE.CODIGO,
  V_PLAN_CONTABLE.NATURALEZA,
  CASE WHEN SUBSTR(V_PLAN_CONTABLE.CODIGO,0,1) IN ('0')
       THEN 'Z' || ID
       ELSE ID 
  END,
  SUBSTR(ORDEN,0,1),
  V_PLAN_CONTABLE.CENTRO_COSTO,
  V_PLAN_CONTABLE.NOMBRE,
  V_PLAN_CONTABLE.TERCERO,
  V_PLAN_CONTABLE.SUCURSAL,
  V_PLAN_CONTABLE.AUXILIAR,
  V_PLAN_CONTABLE.SALDOs$mesfinal$s,
  V_PLAN_CONTABLE.SALDOs$mesI$s,
  V_PLAN_CONTABLE.AJUSTEs$mesfinal$s,
  V_PLAN_CONTABLE.SALDOs$mesfinal$s,
  CASE WHEN V_PLAN_CONTABLE.CLASECUENTA = 'B'
       THEN V_PLAN_CONTABLE.NOMBRE
       ELSE ' '  
  END 
ORDER BY V_PLAN_CONTABLE.COMPANIA,
  V_PLAN_CONTABLE.ANO,
  V_PLAN_CONTABLE.ID
