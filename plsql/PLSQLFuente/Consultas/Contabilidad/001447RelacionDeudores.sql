SELECT * FROM(
SELECT  NVL(ARRENDAMIENTO,0) ARRENDAMIENTO
       ,NVL(SERVPUB,0) SERVPUB
       ,NVL(ATERRIZAJES,0) ATERRIZAJES
       ,NVL(PARQUEOS,0) PARQUEOS
       ,NVL(TASAAERO,0) TASAAERO
       ,NVL(INTXMORA,0) INTXMORA
       ,NVL(OTROS,0) OTROS
       ,NVL(ARRENDAMIENTO,0) +NVL(SERVPUB,0) +NVL(ATERRIZAJES,0) +NVL(PARQUEOS,0) +NVL(TASAAERO,0) +NVL(INTXMORA,0) +NVL(OTROS,0) TOTAL
       ,NOMBRE
       ,FRAYFECHA
       ,COMPANIA
FROM(
WITH RELDEUDORES AS (
SELECT   DETALLE_COMPROBANTE_CNT.COMPANIA
       , DETALLE_COMPROBANTE_CNT.ANO
       , DETALLE_COMPROBANTE_CNT.TIPO_CPTE
       , DETALLE_COMPROBANTE_CNT.COMPROBANTE
       , COMPROBANTE_CNT.FECHA
       , TERCERO.NOMBRE
       , TRUNC(COMPROBANTE_CNT.FECHA_VCN_DOC)-TO_DATE('s$fechaCorte$s','DD/MM/YYYY') DIFERENCIAENDIAS
       , DETALLE_COMPROBANTE_CNT.NRO_DOCUMENTO
       , NVL(SUM(VALOR_DEBITO-ABONOINICIAL),0) VALORDEUDA
       , DETALLE_COMPROBANTE_CNT.CUENTA CODIGO_CUENTA 
  FROM DETALLE_COMPROBANTE_CNT 
       INNER JOIN COMPROBANTE_CNT 
           ON  DETALLE_COMPROBANTE_CNT.COMPANIA    = COMPROBANTE_CNT.COMPANIA 
           AND DETALLE_COMPROBANTE_CNT.ANO         = COMPROBANTE_CNT.ANO 
           AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE   = COMPROBANTE_CNT.TIPO  
           AND DETALLE_COMPROBANTE_CNT.COMPROBANTE = COMPROBANTE_CNT.NUMERO 
       INNER JOIN TIPO_COMPROBANTE 
           ON  DETALLE_COMPROBANTE_CNT.COMPANIA  = TIPO_COMPROBANTE.COMPANIA 
           AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
       LEFT JOIN TERCERO 
           ON  DETALLE_COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA 
           AND DETALLE_COMPROBANTE_CNT.TERCERO  = TERCERO.NIT
           AND DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL
  WHERE DETALLE_COMPROBANTE_CNT.COMPANIA     = s$compania$s 
    AND TRIM(TERCERO.NOMBRE)                       BETWEEN TRIM('s$terceroInicial$s')       AND TRIM('s$terceroFinal$s') 
    AND TIPO_COMPROBANTE.CLASE_CONTABLE      IN  ('V') 
    AND TRUNC(COMPROBANTE_CNT.FECHA)         <= TO_DATE('s$fechaCorte$s','DD/MM/YYYY') 
  GROUP BY DETALLE_COMPROBANTE_CNT.COMPANIA
         , DETALLE_COMPROBANTE_CNT.ANO
         , DETALLE_COMPROBANTE_CNT.TIPO_CPTE
         , DETALLE_COMPROBANTE_CNT.COMPROBANTE
         , COMPROBANTE_CNT.FECHA
         , TERCERO.NOMBRE
         , TRUNC(COMPROBANTE_CNT.FECHA_VCN_DOC)-TO_DATE('s$fechaCorte$s','DD/MM/YYYY')
         , DETALLE_COMPROBANTE_CNT.NRO_DOCUMENTO
         , DETALLE_COMPROBANTE_CNT.CUENTA 
),
RELINGRESOS AS(
SELECT   DETALLE_COMPROBANTE_CNT.COMPANIA 
       , DETALLE_COMPROBANTE_CNT.ANO
       , DETALLE_COMPROBANTE_CNT.TIPO_CPTE_AFECT
       , DETALLE_COMPROBANTE_CNT.CMPTE_AFECTADO
       , NVL(SUM(VALOR_DEBITO+VALOR_CREDITO),0) VALORINGRESO
       , DETALLE_COMPROBANTE_CNT.CUENTA CODIGO_CUENTA 
  FROM DETALLE_COMPROBANTE_CNT 
      LEFT JOIN TERCERO 
          ON  DETALLE_COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA 
          AND DETALLE_COMPROBANTE_CNT.TERCERO  = TERCERO.NIT 
          AND DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL 
      INNER JOIN TIPO_COMPROBANTE 
          ON  DETALLE_COMPROBANTE_CNT.COMPANIA  = TIPO_COMPROBANTE.COMPANIA 
          AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
 WHERE DETALLE_COMPROBANTE_CNT.COMPANIA           = s$compania$s
    AND TRIM(TERCERO.NOMBRE)                       BETWEEN TRIM('s$terceroInicial$s')       AND TRIM('s$terceroFinal$s') 
   AND TIPO_COMPROBANTE.CLASE_CONTABLE            IN ('I','N') 
   AND SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA,0,2) =  '14' 
   AND TRUNC(DETALLE_COMPROBANTE_CNT.FECHA)       <= TO_DATE('s$fechaCorte$s','DD/MM/YYYY') 
   AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE_AFECT    IS NOT NULL 
   AND DETALLE_COMPROBANTE_CNT.CMPTE_AFECTADO     IS NOT NULL 
 GROUP BY DETALLE_COMPROBANTE_CNT.COMPANIA
                , DETALLE_COMPROBANTE_CNT.ANO
                , DETALLE_COMPROBANTE_CNT.TIPO_CPTE_AFECT
                , DETALLE_COMPROBANTE_CNT.CMPTE_AFECTADO
                , DETALLE_COMPROBANTE_CNT.CUENTA  )
SELECT   RELDEUDORES.COMPANIA
       , RELDEUDORES.ANO
       , RELDEUDORES.TIPO_CPTE
       , RELDEUDORES.COMPROBANTE
       , RELDEUDORES.FECHA
       , RELDEUDORES.NOMBRE
       , RELDEUDORES.DIFERENCIAENDIAS
       , RELDEUDORES.NRO_DOCUMENTO
       , RELDEUDORES.VALORDEUDA
       , NVL(VALORINGRESO,0) VALORINGRESO
       , RELDEUDORES.COMPROBANTE||' - '||TO_CHAR(RELDEUDORES.FECHA,'DD/MM/YYYY') FRAYFECHA 
 FROM RELDEUDORES 
     LEFT JOIN RELINGRESOS 
         ON  RELDEUDORES.COMPANIA      = RELINGRESOS.COMPANIA 
         AND RELDEUDORES.ANO           = RELINGRESOS.ANO 
         AND RELDEUDORES.TIPO_CPTE     = RELINGRESOS.TIPO_CPTE_AFECT 
         AND RELDEUDORES.COMPROBANTE   = RELINGRESOS.CMPTE_AFECTADO  
         AND RELDEUDORES.CODIGO_CUENTA = RELINGRESOS.CODIGO_CUENTA  
 WHERE RELDEUDORES.COMPANIA                                        = s$compania$s 
   AND PCK_SYSMAN_UTL.FC_ROUND((VALORDEUDA-NVL(VALORINGRESO,0)),2) > 0 
 )P PIVOT (SUM(VALORDEUDA-VALORINGRESO) FOR NRO_DOCUMENTO IN('0001' ARRENDAMIENTO
                                                             ,'0002' SERVPUB
                                                             ,'0003' ATERRIZAJES
                                                             ,'0004' PARQUEOS
                                                             ,'0005' TASAAERO
                                                             ,'0006' INTXMORA
                                                             , NULL   OTROS))  ) DEUDORES WHERE TOTAL > 0 
