SELECT  COMPANIA, 
          SUBSTR(CODIGO,1,1) CLASE, 
          PCK_SYSMAN_UTL.FC_NOMBRECUENTA(COMPANIA, ANO,SUBSTR(CODIGO,1,1) ) NOMCTA, 
          SUM(CREDITO) SUMADECREDITO, 
         SUM(DEBITO) SUMADEDEBITO, 
         SUM(DEBITO) - SUM(CREDITO) NETO 
  FROM SALDOSINICIALES 
  WHERE COMPANIA = 's$compania$s'  
  AND ANO = s$anoTrabajo$s 
GROUP BY COMPANIA, SUBSTR(CODIGO,1,1), PCK_SYSMAN_UTL.FC_NOMBRECUENTA(COMPANIA, ANO,SUBSTR(CODIGO,1,1) ) 
ORDER BY SUBSTR(CODIGO,1,1) ASC
