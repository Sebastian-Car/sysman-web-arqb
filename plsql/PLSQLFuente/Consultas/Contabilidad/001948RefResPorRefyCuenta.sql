SELECT DETALLE_COMPROBANTE_CNT.REFERENCIA,
  REFERENCIA.COMPANIA,
  REFERENCIA.NOMBRE AS NOMBRE_REFERENCIA,
  DETALLE_COMPROBANTE_CNT.TIPO_CPTE,
  DETALLE_COMPROBANTE_CNT.COMPROBANTE,
  DETALLE_COMPROBANTE_CNT.VALOR_DEBITO,
  DETALLE_COMPROBANTE_CNT.VALOR_CREDITO,
  DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE,
  DETALLE_COMPROBANTE_CNT.ANO,
  DETALLE_COMPROBANTE_CNT.FECHA,
  DETALLE_COMPROBANTE_CNT.CUENTA AS CODIGO_CUENTA,
  PLAN_CONTABLE.NOMBRE AS NOMBRE_CONTABLE,
  REFERENCIA.CODIGO
FROM DETALLE_COMPROBANTE_CNT
INNER JOIN REFERENCIA
 ON DETALLE_COMPROBANTE_CNT.COMPANIA   = REFERENCIA.COMPANIA
AND DETALLE_COMPROBANTE_CNT.ANO        = REFERENCIA.ANO
AND DETALLE_COMPROBANTE_CNT.REFERENCIA = REFERENCIA.CODIGO
INNER JOIN PLAN_CONTABLE
 ON DETALLE_COMPROBANTE_CNT.COMPANIA  = PLAN_CONTABLE.COMPANIA
AND DETALLE_COMPROBANTE_CNT.ANO       = PLAN_CONTABLE.ANO
AND DETALLE_COMPROBANTE_CNT.CUENTA    = PLAN_CONTABLE.CODIGO
WHERE REFERENCIA.COMPANIA          = s$compania$s
AND DETALLE_COMPROBANTE_CNT.ANO    = s$anio$s
AND DETALLE_COMPROBANTE_CNT.FECHA BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
AND DETALLE_COMPROBANTE_CNT.CUENTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s
'
AND REFERENCIA.CODIGO BETWEEN 's$refInicial$s' AND 's$refFinal$s'
AND DETALLE_COMPROBANTE_CNT.REFERENCIA IS NOT NULL
