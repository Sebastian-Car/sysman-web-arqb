SELECT SUBSTR(SALDOAUX.CODIGO,0,1) CLASE,
  SALDOAUX.CODIGO,
  SALDOAUX.NOMBRE,
  CUENTABANCOS.CUENTANUMERO,
  SALDOAUX.SALDOs$mesI$s SALDOBASE,
  SALDOAUX.DEBITOs$mesInicial$s +SALDOAUX.DEBITOs$mesFinal$s SUMADEDEBITO,
  SALDOAUX.CREDITOs$mesInicial$s+SALDOAUX.CREDITOs$mesFinal$s SUMADECREDITO,
  (SALDOAUX.DEBITOs$mesInicial$s+SALDOAUX.DEBITOs$mesFinal$s)-(SALDOAUX.CREDITOs$mesInicial$s+SALDOAUX.CREDITOs$mesFinal$s) NUEVOSALDO,
  SALDOAUX.AJUSTEs$mesFinal$s SALDOEXTRACTO,
  SUBSTR((
  CASE
    WHEN SUBSTR(SALDOAUX.CODIGO,0,1)='0'
    THEN 'Z'
      || SALDOAUX.CODIGO
    ELSE SALDOAUX.CODIGO
  END),0,1) CLASEORDEN,
  SALDOAUX.COMPANIA,
  SALDOAUX.ANO,
  SALDOAUX.CODIGO ID,
  SALDOAUX.TERCERO,
  SALDOAUX.AUXILIAR,
  SALDOAUX.CENTRO_COSTO,
  CASE
    WHEN LPAD( SALDOAUX.CODIGO,1)='0'
    THEN 'Z'
      || SALDOAUX.CODIGO
    ELSE SALDOAUX.CODIGO
  END ORDEN ,
  CASE
    WHEN SALDOAUX.NATURALEZA = 'D'
    THEN ((SALDOAUX.SALDOs$mesI$s + (SALDOAUX.DEBITOs$mesInicial$s +SALDOAUX.DEBITOs$mesFinal$s)) - (SALDOAUX.CREDITOs$mesInicial$s +SALDOAUX.CREDITOs$mesFinal$s))
    ELSE ((SALDOAUX.SALDOs$mesI$s + (SALDOAUX.CREDITOs$mesInicial$s +SALDOAUX.CREDITOs$mesFinal$s) ) - ( SALDOAUX.DEBITOs$mesInicial$s +SALDOAUX.DEBITOs$mesFinal$s))
  END NSALDO
FROM V_PLAN_CONTABLE SALDOAUX
LEFT JOIN CUENTABANCOS
ON SALDOAUX.COMPANIA          = CUENTABANCOS.COMPANIA
AND SALDOAUX.ANO              = CUENTABANCOS.ANO
AND SALDOAUX.CODIGO           = CUENTABANCOS.IDCONTABLE
WHERE SALDOAUX.COMPANIA = s$compania$s
  AND SALDOAUX.ANO      = s$anio$s
  AND SALDOAUX.CODIGO   BETWEEN s$codigoInicial$s AND s$codigoFinal$s
  AND ( SALDOAUX.MAN_AUX_TER = CASE WHEN 1=s$manTer$s THEN -1 ELSE SALDOAUX.MAN_AUX_TER END  AND
        SALDOAUX.MAN_AUX_GEN = CASE WHEN 1=s$manAux$s THEN -1 ELSE SALDOAUX.MAN_AUX_GEN END  AND
        SALDOAUX.MAN_CEN_CTO = CASE WHEN 1=s$manCen$s THEN -1 ELSE SALDOAUX.MAN_CEN_CTO END  AND
        SALDOAUX.MAN_AUX_REF = CASE WHEN 1=s$manRef$s THEN -1 ELSE SALDOAUX.MAN_AUX_REF END  AND
        SALDOAUX.MAN_AUX_FUE = CASE WHEN 1=s$manFue$s THEN -1 ELSE SALDOAUX.MAN_AUX_FUE END  
       )
  AND LENGTH(SALDOAUX.CODIGO)<= s$digitos$s        
s$condicion$s
