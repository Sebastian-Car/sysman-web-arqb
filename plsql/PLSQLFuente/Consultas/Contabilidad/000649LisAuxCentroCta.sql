SELECT V_DETALLE_AUXILIAR_CNT.COMPANIA,
       V_DETALLE_AUXILIAR_CNT.ANO, 
       V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA, 
       V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO,
       V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO_NOM, 
       V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE,
       SUM(V_DETALLE_AUXILIAR_CNT.VALOR_DEBITO)  AS SUMADEBITO,
       SUM(V_DETALLE_AUXILIAR_CNT.VALOR_CREDITO) AS SUMACREDITO,
       SUM(V_DETALLE_AUXILIAR_CNT.BASE_GRAVABLE) AS SUMABASE
FROM  V_DETALLE_AUXILIAR_CNT
WHERE V_DETALLE_AUXILIAR_CNT.COMPANIA = s$compania$s 
  AND V_DETALLE_AUXILIAR_CNT.ANO      = s$anoTrabajo$s 
  AND V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO BETWEEN 's$centroInicial$s'
                                              AND 's$centroFinal$s'
  AND V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA BETWEEN 's$codigoInicial$s'
                                        AND 's$codigoFinal$s'
  AND V_DETALLE_AUXILIAR_CNT.FECHA BETWEEN s$fechaInicial$s
                                       AND s$fechaFinal$s 
GROUP BY V_DETALLE_AUXILIAR_CNT.COMPANIA, 
         V_DETALLE_AUXILIAR_CNT.ANO, 
         V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA, 
         V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO, 
         V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO_NOM, 
         V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE
