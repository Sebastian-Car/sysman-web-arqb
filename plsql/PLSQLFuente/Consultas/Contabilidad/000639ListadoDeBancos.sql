WITH LISTADODEBANCOSBASE AS( 
  SELECT       COMPANIA,  
               ANO,  
               ID,  
               CODIGO,  
               CLASECUENTA,  
               MOVIMIENTO,  
               NOMBRE 
  FROM         V_PLAN_CONTABLE 
  WHERE        COMPANIA    = 's$compania$s' 
    AND        ANO         = s$anio$s 
    AND        CLASECUENTA = 'B' 
    AND        MOVIMIENTO  = -1) 
    SELECT     LISTADODEBANCOSBASE.COMPANIA,  
               LISTADODEBANCOSBASE.ANO,  
               LISTADODEBANCOSBASE.ID,  
               LISTADODEBANCOSBASE.CODIGO,  
               LISTADODEBANCOSBASE.NOMBRE,  
               DETALLE_COMPROBANTE_CNT.TIPO_CPTE,  
               TIPO_COMPROBANTE.NOMBRE TNOMBRE,  
               DETALLE_COMPROBANTE_CNT.COMPROBANTE,  
               TO_CHAR(DETALLE_COMPROBANTE_CNT.FECHA,'DD/MM/YYYY') FECHASTR,  
               TO_CHAR(DETALLE_COMPROBANTE_CNT.FECHA,'DD/MM/YYYY') FECHA,  
               CASE WHEN DETALLE_COMPROBANTE_CNT.DESCRIPCION IS NOT NULL 
                    THEN DETALLE_COMPROBANTE_CNT.DESCRIPCION 
                    ELSE COMPROBANTE_CNT.DESCRIPCION 
                    END DESCRIP, 
               DETALLE_COMPROBANTE_CNT.VALOR_DEBITO,  
               DETALLE_COMPROBANTE_CNT.VALOR_CREDITO, 
               CASE WHEN DETALLE_COMPROBANTE_CNT.NRO_DOCUMENTO IS NOT NULL  
                    THEN DETALLE_COMPROBANTE_CNT.NRO_DOCUMENTO 
                    ELSE COMPROBANTE_CNT.NRO_DOCUMENTO 
                    END  NODOC, 
               CASE WHEN VALOR_DEBITO = 0  
                    THEN VALOR_CREDITO 
                    ELSE VALOR_DEBITO 
                    END  VALORT, 
               CASE WHEN VALOR_DEBITO = 0 
                    THEN VALOR_CREDITO 
                    ELSE VALOR_DEBITO 
                    END  VALORTOTAL,  
               TERCERO.NOMBRE AS TERCERONOMBRE,  
               DETALLE_COMPROBANTE_CNT.TERCERO,  
               DETALLE_COMPROBANTE_CNT.SUCURSAL 
   FROM        LISTADODEBANCOSBASE  
   INNER JOIN   DETALLE_COMPROBANTE_CNT ON (LISTADODEBANCOSBASE.COMPANIA = DETALLE_COMPROBANTE_CNT.COMPANIA  
        AND    LISTADODEBANCOSBASE.ANO = DETALLE_COMPROBANTE_CNT.ANO  
        AND    LISTADODEBANCOSBASE.CODIGO = DETALLE_COMPROBANTE_CNT.CUENTA)  
   LEFT JOIN   TIPO_COMPROBANTE ON (DETALLE_COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA 
        AND    DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO)  
   LEFT JOIN   COMPROBANTE_CNT ON (DETALLE_COMPROBANTE_CNT.COMPANIA = COMPROBANTE_CNT.COMPANIA 
        AND    DETALLE_COMPROBANTE_CNT.TIPO_CPTE = COMPROBANTE_CNT.TIPO 
        AND    DETALLE_COMPROBANTE_CNT.ANO = COMPROBANTE_CNT.ANO 
        AND    DETALLE_COMPROBANTE_CNT.COMPROBANTE = COMPROBANTE_CNT.NUMERO)  
   LEFT JOIN   TERCERO ON (DETALLE_COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA 
        AND    DETALLE_COMPROBANTE_CNT.TERCERO = TERCERO.NIT 
        AND    DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL) 
   WHERE       LISTADODEBANCOSBASE.COMPANIA = 's$compania$s' 
        AND    DETALLE_COMPROBANTE_CNT.TIPO_CPTE BETWEEN 's$tipoInicial$s' AND 's$tipoFinal$s' 
        AND    TO_DATE(TO_CHAR(DETALLE_COMPROBANTE_CNT.FECHA,'DD/MM/YYYY')) BETWEEN s$fechaInicial$s AND s$fechaFinal$s
