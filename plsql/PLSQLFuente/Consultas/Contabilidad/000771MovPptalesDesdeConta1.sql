SELECT  COMPROBANTE_CNT.NUMERO AS NUMEROCONTABLE, 
        COMPROBANTE_CNT.FECHA, 
        COMPROBANTE_CNT.TERCERO, 
        TERCERO.NOMBRE AS NOMBRETER,
        TIPO_COMPROBANTE.NOMBRE AS NOMBRECOMP,
        COMPROBANTE_CNT.DESCRIPCION, 
        COMPROBANTE_CNT.VLR_DOCUMENTO AS VALORDOC, 
        COMPROBANTE_CNT.VLRAGIRAR AS VALORNETO, 
        COMPROBANTE_CNT.TIPO 
FROM COMPROBANTE_CNT 
  LEFT JOIN COMPROBANTE_PPTAL 
    ON COMPROBANTE_CNT.COMPANIA = COMPROBANTE_PPTAL.COMPANIA  
    AND COMPROBANTE_CNT.ANO = COMPROBANTE_PPTAL.ANO  
    AND COMPROBANTE_CNT.TIPO = COMPROBANTE_PPTAL.TIPO
    AND COMPROBANTE_CNT.NUMERO = COMPROBANTE_PPTAL.NUMERO
  INNER JOIN TIPO_COMPROBANTE 
    ON COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA  
    AND COMPROBANTE_CNT.TIPO = TIPO_COMPROBANTE.CODIGO 
  INNER JOIN TERCERO 
    ON COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA  
    AND COMPROBANTE_CNT.TERCERO = TERCERO.NIT 
    AND  COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL
  INNER JOIN CLASECNT 
    ON TIPO_COMPROBANTE.CLASE_CONTABLE = CLASECNT.CODIGO
  s$detalleComprobante$s  
WHERE  COMPROBANTE_CNT.COMPANIA = s$compania$s  
    AND CLASETIPOPPTAL IS NOT NULL 
    AND COMPROBANTE_CNT.TIPO BETWEEN s$tipoInicial$s AND s$tipoFinal$s 
    AND COMPROBANTE_CNT.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s   
    s$numeroPptal$s
    AND COMPROBANTE_CNT.NUMERO IS NOT NULL
