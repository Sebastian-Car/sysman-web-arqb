SELECT V_PLAN_CONTABLE.COMPANIA, 
       V_PLAN_CONTABLE.CODIGO ID,
       SUBSTR(V_PLAN_CONTABLE.CODIGO, 1, 1)  CLASE, 
       V_PLAN_CONTABLE.CODIGO, 
       V_PLAN_CONTABLE.NOMBRE  PLAN_CONTABLE_NOMBRE, 
       V_PLAN_CONTABLE.SALDOs$mesTrabajo$s  SALDO,
       V_PLAN_CONTABLE.NETOs$mesTrabajo$s  NETO,
       V_PLAN_CONTABLE.SALDOs$mesTrabajo1$s SALDOANT, 
       SUBSTR((CASE WHEN SUBSTR(V_PLAN_CONTABLE.CODIGO, 1, 1) = '0' THEN 'Z' || V_PLAN_CONTABLE.CODIGO ELSE V_PLAN_CONTABLE.CODIGO END), 1, 1)  CLASEORDEN, 
       CASE WHEN SUBSTR(V_PLAN_CONTABLE.CODIGO, 1, 1) = '0' THEN 'Z' || V_PLAN_CONTABLE.CODIGO ELSE V_PLAN_CONTABLE.CODIGO END  ORDEN, 
       V_PLAN_CONTABLE.CODIGO || CASE WHEN CENTRO_COSTO IS NULL THEN ' ' ELSE '' END|| 
                            CASE WHEN TERCERO IS NULL THEN ' ' ELSE '' END || 
                            CASE WHEN AUXILIAR IS NULL THEN ' '  ELSE '' END || V_PLAN_CONTABLE.NOMBRE  ORDENCUENTAS, 
       V_PLAN_CONTABLE.ANO,  
       V_PLAN_CONTABLE.AUXILIAR, 
       V_PLAN_CONTABLE.CENTRO_COSTO,
       V_PLAN_CONTABLE.TERCERO

FROM  (s$baseBalance$s) V_PLAN_CONTABLE
s$saldoCero$s
 s$digitosCond$s

ORDER BY 
    V_PLAN_CONTABLE.COMPANIA, 
    V_PLAN_CONTABLE.ANO, 
    V_PLAN_CONTABLE.CODIGO
