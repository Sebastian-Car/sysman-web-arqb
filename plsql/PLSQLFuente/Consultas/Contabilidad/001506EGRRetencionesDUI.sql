SELECT COMPROBANTE_CNTRETENCION.COMPANIA,
  COMPROBANTE_CNTRETENCION.ANO,
  COMPROBANTE_CNTRETENCION.TIPO,
  COMPROBANTE_CNTRETENCION.NUMERO,
  COMPROBANTE_CNTRETENCION.TIPORETENCION,
  COMPROBANTE_CNTRETENCION.CODIGORETENCION,
  RETENCIONES.PCT_APLICAR,
  CASE WHEN LEY1450 <> 0 THEN VALORBASELEY1607 ELSE COMPROBANTE_CNTRETENCION.VALORBASE END VALORBASE,
  TIPORETENCION.NOMBRE,
  COMPROBANTE_CNTRETENCION.VALOR RETENIDO
FROM COMPROBANTE_CNTRETENCION
LEFT JOIN COMPROBANTE_CNT
ON COMPROBANTE_CNTRETENCION.COMPANIA         = COMPROBANTE_CNT.COMPANIA
AND COMPROBANTE_CNTRETENCION.ANO             = COMPROBANTE_CNT.ANO
AND COMPROBANTE_CNTRETENCION.TIPO   = COMPROBANTE_CNT.TIPO
AND COMPROBANTE_CNTRETENCION.NUMERO = COMPROBANTE_CNT.NUMERO
LEFT JOIN COMPROBANTE_CNTAFECTADOS
ON COMPROBANTE_CNTAFECTADOS.COMPANIA           = COMPROBANTE_CNT.COMPANIA
AND COMPROBANTE_CNTAFECTADOS.ANO_AFECT         = COMPROBANTE_CNT.ANO
AND COMPROBANTE_CNTAFECTADOS.TIPO_CPTE_AFECT   = COMPROBANTE_CNT.TIPO
AND COMPROBANTE_CNTAFECTADOS.COMPROBANTE_AFECT = COMPROBANTE_CNT.NUMERO
LEFT JOIN RETENCIONES
ON COMPROBANTE_CNTRETENCION.COMPANIA         = RETENCIONES.COMPANIA
AND COMPROBANTE_CNTRETENCION.ANO             = RETENCIONES.ANO
AND COMPROBANTE_CNTRETENCION.TIPORETENCION   = RETENCIONES.TIPO
AND COMPROBANTE_CNTRETENCION.CODIGORETENCION = RETENCIONES.CODIGO
LEFT JOIN TIPORETENCION
ON COMPROBANTE_CNTRETENCION.TIPORETENCION = TIPORETENCION.CODIGO 
WHERE COMPROBANTE_CNTRETENCION.COMPANIA   = $P{PR_COMPANIA}   
AND COMPROBANTE_CNTRETENCION.ANO          = $P{PR_ANO}
AND COMPROBANTE_CNTRETENCION.TIPO         = $P{PR_TIPO}
AND COMPROBANTE_CNTRETENCION.NUMERO = $P{PR_COMPROBANTE}
s$condicion$s
ORDER BY COMPROBANTE_CNTRETENCION.TIPORETENCION, 
COMPROBANTE_CNTRETENCION.CODIGORETENCION
