SELECT DETALLE_COMPROBANTE_CNT.COMPANIA,
DETALLE_COMPROBANTE_CNT.ANO,
DETALLE_COMPROBANTE_CNT.TIPO_CPTE,
DETALLE_COMPROBANTE_CNT.CUENTA CODIGO_CUENTA,
SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO) AS SUMADEVALOR_DEBITO, 
SUM(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO) AS SUMADEVALOR_CREDITO,
TIPO_COMPROBANTE.NOMBRE TIPO_COMPROBANTE_NOMBRE, 
 TO_DATE(TO_CHAR(DETALLE_COMPROBANTE_CNT.FECHA,'DD/MM/YYYY')) FECHA , 
PLAN_CONTABLE.NOMBRE PLAN_CONTABLE_NOMBRE
FROM DETALLE_COMPROBANTE_CNT 
    INNER JOIN TIPO_COMPROBANTE ON 
     DETALLE_COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA AND 
     DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO    
   INNER JOIN  PLAN_CONTABLE ON
     DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA AND 
     DETALLE_COMPROBANTE_CNT.ANO            = PLAN_CONTABLE.ANO  AND 
     DETALLE_COMPROBANTE_CNT.CUENTA                = PLAN_CONTABLE.CODIGO
WHERE
  DETALLE_COMPROBANTE_CNT.COMPANIA = s$compania$s
AND TO_DATE(TO_CHAR(FECHA,'DD/MM/YYYY')) BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY') 
AND TIPO_CPTE BETWEEN s$tipoInicial$s AND s$tipoFinal$s
GROUP BY DETALLE_COMPROBANTE_CNT.COMPANIA, 
DETALLE_COMPROBANTE_CNT.FECHA,
DETALLE_COMPROBANTE_CNT.ANO,
DETALLE_COMPROBANTE_CNT.TIPO_CPTE,
DETALLE_COMPROBANTE_CNT.CUENTA, 
TIPO_COMPROBANTE.NOMBRE, 
PLAN_CONTABLE.NOMBRE
