SELECT SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO) AS VALOR_DEBITO, 
       SUM(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO) AS VALOR_CREDITO, 
       TRUNC(DETALLE_COMPROBANTE_CNT.FECHA) FECHA, 
       PLAN_CONTABLE.NOMBRE CUENTANOMBRE, 
       CASE WHEN s$agrupar$s=4
            THEN SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA,0,6)
            ELSE SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA,0,2)
            END NIVEL
FROM DETALLE_COMPROBANTE_CNT 
  INNER JOIN V_PLAN_CONTABLE PLAN_CONTABLE 
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA  
    AND DETALLE_COMPROBANTE_CNT.ANO      = PLAN_CONTABLE.ANO 
    AND CASE WHEN s$agrupar$s = 4
            THEN SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA,0,6)
            ELSE SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA,0,2)
            END = PLAN_CONTABLE.ID   
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA = s$compania$s  
  AND DETALLE_COMPROBANTE_CNT.ANO      = s$anoTrabajo$s 
  AND DETALLE_COMPROBANTE_CNT.MES      = s$mesTrabajo$s 
GROUP BY TRUNC(DETALLE_COMPROBANTE_CNT.FECHA), 
         PLAN_CONTABLE.NOMBRE, 
         CASE WHEN s$agrupar$s=4
            THEN SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA,0,6)
            ELSE SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA,0,2)
            END
ORDER BY 3,5
