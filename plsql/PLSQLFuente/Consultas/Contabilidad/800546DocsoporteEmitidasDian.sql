MERGE INTO CONSULTAS FIN USING (SELECT '800546DocsoporteEmitidasDian' INFORME ,TO_CLOB(q'[SELECT
    ESTADOFAC.NUMERO,
    ESTADOFAC.PREFIJO,
    ESTADOFAC.ESTADO,
    ESTADOFAC.TOTAL VALOR,
    ESTADOFAC.FECHA,
    COMPROBANTE_CNT.TERCERO,
    ESTADO_DOC_DIAN.DOCUMENTKEY CUDS,
    (
    CASE
        COMPROBANTE_CNT.ANULADO WHEN -1 THEN 'ANULADA'
        ELSE 'NO ANULADA'
    END )ANULA,
    (
    CASE
       COMPROBANTE_CNT.IND_PAGADO WHEN -1 THEN 'RECAUDADO'
       ELSE 'NO RECAUDADO'
    END )RECAUDA,
    ESTADOFAC.OBSERVACION,
    ESTADO_DOC_DIAN.MENSAJE 
FROM
    ESTADOFAC
    INNER JOIN ESTADO_DOC_DIAN ON 
ESTADOFAC.NUMERO = ESTADO_DOC_DIAN.NUM_DOCUMENTO 
JOIN COMPROBANTE_CNT ON COMPANIA = 's$compania$s' AND
    ESTADOFAC.NUMERO = COMPROBANTE_CNT.CONSECUTIVODIAN AND COMPROBANTE_CNT.TIPO = 's$tipo$s'
ORDER BY
    COMPROBANTE_CNT.CONSECUTIVODIAN DESC]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);