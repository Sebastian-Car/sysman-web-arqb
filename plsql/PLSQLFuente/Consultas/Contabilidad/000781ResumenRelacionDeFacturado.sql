SELECT 
	COMPROBANTE_CNT.FECHA,
	DETALLE_COMPROBANTE_CNT.CUENTA CODIGO_CUENTA, 
	SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO)  AS SUMADEVALOR_DEBITO, 
	SUM(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO) AS SUMADEVALOR_CREDITO, 
	PLAN_CONTABLE.NOMBRE,
	PLAN_CONTABLE.COMPANIA
FROM COMPROBANTE_CNT 
	INNER JOIN DETALLE_COMPROBANTE_CNT 
		ON COMPROBANTE_CNT.COMPANIA = DETALLE_COMPROBANTE_CNT.COMPANIA
		AND COMPROBANTE_CNT.ANO = DETALLE_COMPROBANTE_CNT.ANO
		AND COMPROBANTE_CNT.TIPO = DETALLE_COMPROBANTE_CNT.TIPO_CPTE
		AND COMPROBANTE_CNT.NUMERO = DETALLE_COMPROBANTE_CNT.COMPROBANTE
	INNER JOIN COMPROBANTE_CNTAFECTADOS
		ON DETALLE_COMPROBANTE_CNT.COMPANIA = COMPROBANTE_CNTAFECTADOS.COMPANIA 
		AND DETALLE_COMPROBANTE_CNT.ANO  =COMPROBANTE_CNTAFECTADOS.ANO 
		AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = COMPROBANTE_CNTAFECTADOS.TIPO_CPTE 
		AND DETALLE_COMPROBANTE_CNT.COMPROBANTE = COMPROBANTE_CNTAFECTADOS.COMPROBANTE 
		AND DETALLE_COMPROBANTE_CNT.ANO_AFECT = COMPROBANTE_CNTAFECTADOS.ANO_AFECT 
		AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE_AFECT = COMPROBANTE_CNTAFECTADOS.TIPO_CPTE_AFECT 
		AND DETALLE_COMPROBANTE_CNT.CMPTE_AFECTADO = COMPROBANTE_CNTAFECTADOS.COMPROBANTE_AFECT
	INNER JOIN PLAN_CONTABLE
		ON DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA 
		AND DETALLE_COMPROBANTE_CNT.ANO = PLAN_CONTABLE.ANO 
		AND DETALLE_COMPROBANTE_CNT.CUENTA = PLAN_CONTABLE.CODIGO
	INNER JOIN TIPO_COMPROBANTE 
		ON DETALLE_COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA
		AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO 
	INNER JOIN TIPO_COMPROBANTE TIPO_COMPROBANTE_1 
		ON DETALLE_COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE_1.COMPANIA
		AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE_AFECT = TIPO_COMPROBANTE_1.CODIGO
WHERE	TIPO_COMPROBANTE.COMPANIA = s$compania$s
		AND TIPO_COMPROBANTE.CLASE_CONTABLE IN ('I','S')
		AND TIPO_COMPROBANTE_1.CLASE_CONTABLE IN ('V')  
		AND DETALLE_COMPROBANTE_CNT.CMPTE_AFECTADO NOT IN(0) 
		AND COMPROBANTE_CNT.FECHA = s$fecha$s
GROUP BY 
	COMPROBANTE_CNT.FECHA, 
	DETALLE_COMPROBANTE_CNT.CUENTA, 
	PLAN_CONTABLE.NOMBRE, 
	PLAN_CONTABLE.COMPANIA
