SELECT  DETALLE_COMPROBANTE_CNT.COMPANIA,
        DETALLE_COMPROBANTE_CNT.TIPO_CPTE, 
        TIPO_COMPROBANTE.NOMBRE NOMBRE_CPTE, 
        SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA ,1,s$digitos$s) GRUPO, 
        C_NOMGRUPO.NOMBRE NOMCTA_GRUPO, 
        SUM(VALOR_DEBITO) DEBITOS,
        SUM(VALOR_CREDITO) CREDITOS
FROM DETALLE_COMPROBANTE_CNT 
  INNER JOIN PLAN_CONTABLE  V_PLAN_CONTABLE
    ON DETALLE_COMPROBANTE_CNT.COMPANIA =  V_PLAN_CONTABLE.COMPANIA 
   AND  DETALLE_COMPROBANTE_CNT.ANO = V_PLAN_CONTABLE.ANO 
   AND  DETALLE_COMPROBANTE_CNT.CUENTA = V_PLAN_CONTABLE.CODIGO 
  INNER JOIN PLAN_CONTABLE C_NOMGRUPO
    ON DETALLE_COMPROBANTE_CNT.COMPANIA =  C_NOMGRUPO.COMPANIA 
   AND  DETALLE_COMPROBANTE_CNT.ANO = C_NOMGRUPO.ANO 
   AND  SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA ,1,6) = C_NOMGRUPO.CODIGO 
  INNER JOIN TIPO_COMPROBANTE 
    ON DETALLE_COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA
   AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA = 's$compania$s' 
AND DETALLE_COMPROBANTE_CNT.ANO = s$anoTrabajo$s
AND DETALLE_COMPROBANTE_CNT.MES = s$mesTrabajo$s
AND V_PLAN_CONTABLE.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'   
  
GROUP BY DETALLE_COMPROBANTE_CNT.COMPANIA, 
        DETALLE_COMPROBANTE_CNT.TIPO_CPTE, 
        SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA ,1,s$digitos$s),
        TIPO_COMPROBANTE.NOMBRE, 
        C_NOMGRUPO.NOMBRE
