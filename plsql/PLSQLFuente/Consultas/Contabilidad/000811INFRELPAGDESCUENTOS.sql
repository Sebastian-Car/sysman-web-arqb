SELECT TIPO_COMPROBANTE.CLASE_CONTABLE, 
       TERCERO.NOMBRE, 
       COMPROBANTE_CNT.CUENTA, 
       COMPROBANTE_CNT.VLRAGIRAR, 
       PLAN_CONTABLE.NOMBRE BANCO, 
       COMPROBANTE_CNT.DESCRIPCION, 
       COMPROBANTE_CNT.NRO_DOCUMENTO, 
       COMPROBANTE_CNT.TERCERO, 
       COMPROBANTE_CNT.COMPANIA
FROM COMPROBANTE_CNT 
  INNER JOIN TIPO_COMPROBANTE 
    ON  COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA 
    AND COMPROBANTE_CNT.TIPO     = TIPO_COMPROBANTE.CODIGO 
  LEFT JOIN PLAN_CONTABLE 
    ON  COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA 
    AND COMPROBANTE_CNT.ANO      = PLAN_CONTABLE.ANO 
    AND COMPROBANTE_CNT.CUENTA   = PLAN_CONTABLE.CODIGO 
  LEFT JOIN TERCERO 
    ON  COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA 
    AND COMPROBANTE_CNT.TERCERO  = TERCERO.NIT 
    AND COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL 
WHERE COMPROBANTE_CNT.COMPANIA  = s$compania$s 
  AND COMPROBANTE_CNT.ANO       = s$anio$s 
  AND EXTRACT(MONTH FROM FECHA) = s$mes$s
  AND COMPROBANTE_CNT.TERCERO   BETWEEN 's$terceroInicial$s' 
                                    AND 's$terceroFinal$s' 
  AND TIPO_COMPROBANTE.CLASE_CONTABLE = 'E' 
ORDER BY COMPROBANTE_CNT.CUENTA
