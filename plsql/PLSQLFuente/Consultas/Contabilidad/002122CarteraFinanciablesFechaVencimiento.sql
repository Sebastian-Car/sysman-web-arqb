       TO_CHAR(COMPROBANTECARTERA) COMPROBANTECARTERA,
       TERCERO,
       TABLA.SUCURSAL,
       TABLA.NOMBRE,
       DESCRIPCION,
       SUM(TOTALDEUDA) TOTALDEUDA,
       CASE WHEN DIFERENCIAENDIAST <= 0 THEN 0 ELSE DIFERENCIAENDIAST END AS DIFERENCIAENDIAST,
       SUM(CASE WHEN DIFERENCIAENDIAST BETWEEN -1 AND 30 THEN SALDOT ELSE 0 END) AS SALDO1,
       SUM(CASE WHEN DIFERENCIAENDIAST BETWEEN 31 AND 60 THEN SALDOT ELSE 0 END) AS SALDO2,
       SUM(CASE WHEN DIFERENCIAENDIAST BETWEEN 61 AND 90 THEN SALDOT ELSE 0 END) AS SALDO3,
