        CASE WHEN TERCERO.CLASEENTIDADOFICIAL <> 0 THEN 'Entidades Oficiales' ELSE 'Particulares' END
        ELSE 'Independiente / Particular' END AS CLASE,
    SUM( (DETALLE_COMPROBANTE_CNT.VALOR_DEBITO-DETALLE_COMPROBANTE_CNT.VALOR_CREDITO)
     -CASE WHEN DETALLE_COMPROBANTE_CNT.FECHA_ABONOINICIAL<= TO_DATE('s$fechacorte$s','DD/MM/YYYY') THEN DETALLE_COMPROBANTE_CNT.ABONOINICIAL ELSE 0 END)
     -NVL(AFEC.VALOR_AFECTACION,0) AS SALDOT,
     CASE WHEN  TO_DATE('s$fechacorte$s','DD/MM/YYYY') < COMPROBANTE_CNT.FECHA_VCN_DOC THEN -1 ELSE 
    PCK_SYSMAN_UTL.FC_DIASMESCOMERCIAL(TO_DATE('s$fechacorte$s','DD/MM/YYYY'),(COMPROBANTE_CNT.FECHA_VCN_DOC ))-1 END AS  DIFERENCIAENDIAST,
    MAX(CONCAT(CONCAT(CONCAT(CONCAT(DETALLE_COMPROBANTE_CNT.DESCRIPCION,'-'),DETALLE_COMPROBANTE_CNT.TIPO_CPTE),'-'),DETALLE_COMPROBANTE_CNT.COMPROBANTE)) DESCRIPCION,
    1 AS TIPOINFORME,
    SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO) VALOR_DEBITO,
    SUM(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO) VALOR_CREDITO,
    NVL(AFEC.DEBITO_AFECTADO,0) DEBITO_AFECTADO,
    NVL(AFEC.CREDITO_AFECTADO,0) CREDITO_AFECTADO,
    SUM(CASE WHEN DETALLE_COMPROBANTE_CNT.FECHA_ABONOINICIAL<= TO_DATE('s$fechacorte$s','DD/MM/YYYY') THEN DETALLE_COMPROBANTE_CNT.ABONOINICIAL ELSE 0 END)  ABONOINICIAL
    FROM DETALLE_COMPROBANTE_CNT INNER JOIN TERCERO
      ON DETALLE_COMPROBANTE_CNT.TERCERO = TERCERO.NIT
     A]') || TO_CLOB(q'[ND DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL
     AND DETALLE_COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA
    INNER JOIN COMPROBANTE_CNT ON DETALLE_COMPROBANTE_CNT.COMPANIA = COMPROBANTE_CNT.COMPANIA
                                  AND COMPROBANTE_CNT.ANO = DETALLE_COMPROBANTE_CNT.ANO
                                  AND COMPROBANTE_CNT.TIPO = DETALLE_COMPROBANTE_CNT.TIPO_CPTE
                                  AND COMPROBANTE_CNT.NUMERO = DETALLE_COMPROBANTE_CNT.COMPROBANTE     
    INNER JOIN TIPO_COMPROBANTE
      ON DETALLE_COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA
     AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
    LEFT JOIN (SELECT DET.COMPANIA,
                      DET.TERCERO,
                      DET.SUCURSAL,
                      DET.CMPTE_AFECTADO AS FACTURA,
                      DET.TIPO_CPTE_AFECT AS TIPO_FACTURA,
                      DET.CUENTA,
                      SUM((NVL(DET.VALOR_CREDITO,0)-NVL(DET.VALOR_DEBITO,0))) VALOR_AFECTACION,
                      SUM((NVL(DET.VALOR_DEBITO,0))) DEBITO_AFECTADO,
                      SUM((NVL(DET.VALOR_CREDITO,0))) CREDITO_AFECTADO
               FROM  TIPO_COMPROBANTE TC INNER JOIN COMPROBANTE_CNT CN
                  ON CN.COMPANIA = TC.COMPANIA
                 AND CN.TIPO     = TC.CODIGO
               INNER JOIN  DETALLE_COMPROBANTE_CNT DET
                  ON CN.NUMERO = DET.COMPROBANTE
                 AND CN.TIPO = DET.TIPO_CPTE
                 AND CN.ANO = DET.ANO
                 AND CN.COMPANIA = DET.COMPANIA
              INNER JOIN PLAN_CONTABLE PLAN
                ON PLAN.COMPANIA = DET.COMPANIA
                AND PLAN.ANO      = DET.ANO
                AND PLAN.CODIGO  = DET.CUENTA               
              WHERE DET.COMPANIA = 's$compania$s'
                 AND DET.ANO <= s$ano$s
                 --PILAS CON FECHA
                 AND CN.FECHA_VCN_DOC <= TO_DATE('s$fechacorte$s','DD/MM/YYYY')
                 AND TC.CLASE_CONTABLE In ('I','B','N','C')
                 AND DET.CMPTE_AFECTADO<>0
                 AND PLAN.CLASECUENTA In ('A','C')
             GROUP BY DET.COMPANIA,
                      DET.TERCERO,
                      DET.SUCURSAL,
                      DET.CMPTE_AFECTADO,
                      DET.TIPO_CPTE_AFECT,
                      DET.CUENTA   
        ) AFEC
        ON DETALLE_COMPROBANTE_CNT.CUENTA      = AFEC.CUENTA
       AND DETALLE_COMPROBANTE_CNT.COMPROBANTE = AFEC.FACTURA
       AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE   = AFEC.TIPO_FACTURA
       AND DETALLE_COMPROBANTE_CNT.COMPANIA    = AFEC.COMPANIA
       AND DETALLE_COMPROBANTE_CNT.TERCERO     = AFEC.TERCERO
       AND DETALLE_COMPROBANTE_CNT.SUCURSAL    = AFEC.SUCURSAL      
       INNER JOIN PLAN_CONTABLE
                ON PLAN_CONTABLE.COMPANIA = DETALLE_COMPROBANTE_CNT.COMPANIA
                AND PLAN_CONTABLE.ANO      = DETALLE_COMPROBANTE_CNT.ANO
                AND PLAN_CONTABLE.CODIGO  = DETALLE_COMPROBANTE_CNT.CUENTA
    ]') || TO_CLOB(q'[WHERE DETALLE_COMPROBANTE_CNT.COMPANIA = 's$compania$s'
      AND DETALLE_COMPROBANTE_CNT.ANO <= s$ano$s
      AND DETALLE_COMPROBANTE_CNT.FECHA BETWEEN TO_DATE('s$fechadesde$s','DD/MM/YYYY') AND TO_DATE('s$fechahasta$s','DD/MM/YYYY')
      AND TIPO_COMPROBANTE.CLASE_CONTABLE='V'
      AND PLAN_CONTABLE.CLASECUENTA In ('A','C')
    GROUP BY
    DETALLE_COMPROBANTE_CNT.ANO,
    DETALLE_COMPROBANTE_CNT.TIPO_CPTE,
    DETALLE_COMPROBANTE_CNT.COMPANIA,
    DETALLE_COMPROBANTE_CNT.COMPROBANTE,  
    DETALLE_COMPROBANTE_CNT.TERCERO,
    DETALLE_COMPROBANTE_CNT.SUCURSAL,
    DETALLE_COMPROBANTE_CNT.CUENTA,
    COMPROBANTE_CNT.FECHA_VCN_DOC,
    TERCERO.NOMBRE,
    CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE = 'V' THEN
        CASE WHEN TERCERO.CLASEENTIDADOFICIAL <> 0 THEN 'Entidades Oficiales' ELSE 'Particulares' END
        ELSE 'Independiente / Particular' END,
    PCK_SYSMAN_UTL.FC_DIASMESCOMERCIAL(TO_DATE('s$fechacorte$s','DD/MM/YYYY'),(COMPROBANTE_CNT.FECHA_VCN_DOC))-1,
    1,
    NVL(AFEC.DEBITO_AFECTADO,0),
    NVL(AFEC.CREDITO_AFECTADO,0),
    NVL(AFEC.VALOR_AFECTACION,0)     
) TABLA
INNER JOIN PLAN_CONTABLE ON TABLA.COMPANIA = PLAN_CONTABLE.COMPANIA
AND TABLA.ANO = PLAN_CONTABLE.ANO
AND TABLA.CUENTA = PLAN_CONTABLE.CODIGO
WHERE 
TABLA.COMPANIA = 's$compania$s'
AND ( PLAN_CONTABLE.CODIGO BETWEEN 's$cuentainicial$s' and 's$cuentafinal$s'
OR  PLAN_CONTABLE.COD_EQUI_CARTERA BETWEEN 's$cuentainicial$s' AND 's$cuentafinal$s')
GROUP BY TIPO_CPTE, COMPROBANTECARTERA,
       TERCERO,
       TABLA.SUCURSAL,
       TABLA.NOMBRE,
       DESCRIPCION,
       DIFERENCIAENDIAST,
       CLASE,
       TIPOINFORME,
       FECHA
HAVING SUM(SALDOT) > 0
ORDER BY TERCERO, TIPO_CPTE, COMPROBANTECARTERA]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);