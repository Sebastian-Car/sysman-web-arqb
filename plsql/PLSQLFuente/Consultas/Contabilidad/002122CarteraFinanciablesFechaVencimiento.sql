       SUM(CASE WHEN DIFERENCIAENDIAST BETWEEN 181 AND 270 THEN SALDOT ELSE 0 END) AS SALDO5,
       SUM(CASE WHEN DIFERENCIAENDIAST BETWEEN 271 AND 360 THEN SALDOT ELSE 0 END) AS SALDO6,
       SUM(CASE WHEN DIFERENCIAENDIAST >360 THEN SALDOT ELSE 0 END) AS SALDO7,
       SUM(SALDOT) SALDOT,
       CLASE,
       TIPOINFORME,
       TO_CHAR(FECHA,'DD/MM/YYYY') FECHA
FROM (
    SELECT    
    SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO-DETALLE_COMPROBANTE_CNT.VALOR_CREDITO) AS TOTALDEUDA,
    DETALLE_COMPROBANTE_CNT.ANO,
    DETALLE_COMPROBANTE_CNT.TIPO_CPTE,
    DETALLE_COMPROBANTE_CNT.COMPANIA,
    DETALLE_COMPROBANTE_CNT.COMPROBANTE AS COMPROBANTECARTERA,  
    DETALLE_COMPROBANTE_CNT.TERCERO,
    DETALLE_COMPROBANTE_CNT.SUCURSAL,
    DETALLE_COMPROBANTE_CNT.CUENTA,
    COMPROBANTE_CNT.FECHA_VCN_DOC FECHA,
