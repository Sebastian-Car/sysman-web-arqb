SELECT DETALLE_COMPROBANTE_PPTAL_1.TIPO_CPTE TIPORESERVA,
  DETALLE_COMPROBANTE_PPTAL_1.COMPROBANTE NUMRESERVA,
  DETALLE_COMPROBANTE_PPTAL_1.TIPO_CPTE_AFECT TIPODIS,
  DETALLE_COMPROBANTE_PPTAL_1.CMPTE_AFECTADO NUMDIS,
  SUBCOM_DCGRUPO.COMP,
  SUBCOM_DCGRUPO.ANOCOMP,
  SUBCOM_DCGRUPO.TIPOCOMP,
  SUBCOM_DCGRUPO.NUMCOMP,
  SUBCOM_DCGRUPO.NOMBRE,
  SUBCOM_DCGRUPO.NATURALEZA,
  SUBCOM_DCGRUPO.DEBITO,
  SUBCOM_DCGRUPO.CREDITO,
  SUBCOM_DCGRUPO.TIPO_CPTE_AFECT,
  SUBCOM_DCGRUPO.CMPTE_AFECTADO,
  SUBCOM_DCGRUPO.CUENTA,
  SUBCOM_DCGRUPO.VALOR,
  SUBCOM_DCGRUPO.VLR,
  SUBCOM_DCGRUPO.CONSECUTIVOPPTO,
  SUBCOM_DCGRUPO.DESTINO
FROM (SELECT 
  DETALLE_COMPROBANTE_PPTAL.COMPANIA COMP,
  DETALLE_COMPROBANTE_PPTAL.ANO           ANOCOMP,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE     TIPOCOMP,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE   NUMCOMP,
  PLAN_PRESUPUESTAL.NOMBRE,
  PLAN_PRESUPUESTAL.NATURALEZA,
  SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO) DEBITO,
  SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) CREDITO,
  NVL(DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT,' ') TIPO_CPTE_AFECT,
  DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO,
  DETALLE_COMPROBANTE_PPTAL.CUENTA,
  CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA = 'C'
       THEN (SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)-SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO))
       ELSE (SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO)-SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO))
  END VALOR,
  CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA = 'C'
       THEN (SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)-SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO))
       ELSE (SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO)-SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO))
  END VLR,
  DETALLE_COMPROBANTE_PPTAL.CONSECUTIVOPPTO,
  PLAN_PRESUPUESTAL.DESTINO
FROM DETALLE_COMPROBANTE_PPTAL
INNER JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO     = PLAN_PRESUPUESTAL.ANO
   AND DETALLE_COMPROBANTE_PPTAL.ID   = PLAN_PRESUPUESTAL.ID
LEFT JOIN DETALLE_COMPROBANTE_CNT
    ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA        = DETALLE_COMPROBANTE_CNT.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO             = DETALLE_COMPROBANTE_CNT.ANO
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT = DETALLE_COMPROBANTE_CNT.TIPO_CPTE
    AND DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO  = DETALLE_COMPROBANTE_CNT.COMPROBANTE
    AND DETALLE_COMPROBANTE_PPTAL.CONSECUTIVOPPTO  = DETALLE_COMPROBANTE_CNT.CONSECUTIVO       
WHERE     DETALLE_COMPROBANTE_PPTAL.COMPANIA            = $P{PR_COMPANIA}
AND DETALLE_COMPROBANTE_PPTAL.ANO                       = $P{PR_ANO}
AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE                 = $P{PR_TIPO}
AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE               = $P{PR_NUMERO}
GROUP BY DETALLE_COMPROBANTE_PPTAL.COMPANIA,
         DETALLE_COMPROBANTE_PPTAL.ANO,
         DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
         DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
         PLAN_PRESUPUESTAL.NOMBRE,
         PLAN_PRESUPUESTAL.NATURALEZA,
         NVL(DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT,' '),
         DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO,
         DETALLE_COMPROBANTE_PPTAL.CUENTA,
         DETALLE_COMPROBANTE_PPTAL.CONSECUTIVOPPTO,
         PLAN_PRESUPUESTAL.DESTINO) 
         SUBCOM_DCGRUPO
LEFT JOIN DETALLE_COMPROBANTE_PPTAL DETALLE_COMPROBANTE_PPTAL_1
ON SUBCOM_DCGRUPO.CONSECUTIVOPPTO  = DETALLE_COMPROBANTE_PPTAL_1.CONSECUTIVO
AND SUBCOM_DCGRUPO.CUENTA                   = DETALLE_COMPROBANTE_PPTAL_1.CUENTA
AND SUBCOM_DCGRUPO.CMPTE_AFECTADO = DETALLE_COMPROBANTE_PPTAL_1.COMPROBANTE
AND SUBCOM_DCGRUPO.TIPO_CPTE_AFECT = DETALLE_COMPROBANTE_PPTAL_1.TIPO_CPTE
AND SUBCOM_DCGRUPO.ANOCOMP               = DETALLE_COMPROBANTE_PPTAL_1.ANO
AND SUBCOM_DCGRUPO.COMP                      = DETALLE_COMPROBANTE_PPTAL_1.COMPANIA
