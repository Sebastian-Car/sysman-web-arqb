MERGE INTO CONSULTAS FIN USING (SELECT '002853ConciliacionConPartidas' INFORME ,TO_CLOB(q'[SELECT P.COMPANIA,  
    P.CODIGO,  
    SALDOs$mes$s AS SALDOCONTABLE,  
    SALDOs$mesAnterior$s AS SALDOCONTABLEANTERIOR,  
    MAX(D.CONCILIADOR) AS CONCILIADOR
FROM PLAN_CONTABLE P
LEFT JOIN (
    SELECT COMPANIA, ANO, CUENTA, CONCILIADOR
    FROM DETALLE_COMPROBANTE_CNT
    WHERE COMPANIA = s$compania$s
      AND ANO = s$ano$s
      AND MES = s$mes$s
      AND CUENTA = 's$codCuenta$s') D
ON P.COMPANIA = D.COMPANIA
AND P.ANO = D.ANO
AND P.CODIGO = D.CUENTA
WHERE P.COMPANIA = s$compania$s
  AND P.ANO = s$ano$s
  AND P.CODIGO = 's$codCuenta$s'
GROUP BY P.COMPANIA, P.CODIGO, SALDOs$mes$s, SALDOs$mesAnterior$s]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);