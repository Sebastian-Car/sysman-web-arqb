  V_DETALLE_AUXILIAR_CNT.DIGITOVERIFICACION,
  V_DETALLE_AUXILIAR_CNT.TERCERO_COM_NOM NOMBRETERCERO,
  V_DETALLE_AUXILIAR_CNT.TERCERO_COM_DIR DIRECCION1,
  V_DETALLE_AUXILIAR_CNT.TERCERO_COM_TEL TELEFONOS1,
  V_DETALLE_AUXILIAR_CNT.TERCERO_COM_TIPOID TIPOID,
  ORDENADOR.NOMBRE NOMBREORD,
  ORDENADOR.CARGO CARGOORD,
V_DETALLE_AUXILIAR_CNT.TIPO_COMPROBANTE_NOM NOMBRETPCOM,
  USUARIO.NOMBRE1||' '||USUARIO.NOMBRE2||' '||USUARIO.APELLIDO1||' '||USUARIO.APELLIDO2 CREADOR
FROM V_DETALLE_AUXILIAR_CNT
LEFT JOIN ORDENADOR
ON V_DETALLE_AUXILIAR_CNT.COMPANIA          = ORDENADOR.COMPANIA
AND V_DETALLE_AUXILIAR_CNT.ORDENADOR        = ORDENADOR.CEDULA
LEFT JOIN USUARIO
ON V_DETALLE_AUXILIAR_CNT.CREATED_BY = USUARIO.CODIGO
WHERE V_DETALLE_AUXILIAR_CNT.COMPANIA    = s$compania$s
  AND V_DETALLE_AUXILIAR_CNT.ANO         = s$ano$s
  AND V_DETALLE_AUXILIAR_CNT.TIPO_CPTE   = 's$tipoCpte$s'
  AND V_DETALLE_AUXILIAR_CNT.COMPROBANTE BETWEEN s$numeroPptoInicial$s AND s$numeroPptoFinal$s
