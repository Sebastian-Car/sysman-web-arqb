      || PCK_SYSMAN_UTL.FC_CODIGO_CNT (V_DETALLE_AUXILIAR_CNT.COMPANIA, V_DETALLE_AUXILIAR_CNT.ANO,V_DETALLE_AUXILIAR_CNT.CUENTA_COMP, V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO, V_DETALLE_AUXILIAR_CNT.TERCERO,V_DETALLE_AUXILIAR_CNT.SUCURSAL, V_DETALLE_AUXILIAR_CNT.AUXILIAR, V_DETALLE_AUXILIAR_CNT.REFERENCIA, V_DETALLE_AUXILIAR_CNT.FUENTE_RECURSOS)
  END SEC,
