  V_DETALLE_AUXILIAR_CNT.ANO ANOCOMP,
  V_DETALLE_AUXILIAR_CNT.COMPROBANTE NUMERO,
  V_DETALLE_AUXILIAR_CNT.FECHA FECHACPCNT,
  V_DETALLE_AUXILIAR_CNT.DESCRIPCION DESCRIPCIONCPCNT,
  V_DETALLE_AUXILIAR_CNT.TEXTO,
  V_DETALLE_AUXILIAR_CNT.FECHA_VCN_DOC,
  V_DETALLE_AUXILIAR_CNT.NRO_DOCUMENTO NRODOCUMENTOCPCNT,
  V_DETALLE_AUXILIAR_CNT.TERCERO_COM TERCEROCPCNT,
  V_DETALLE_AUXILIAR_CNT.COMPROBANTE NUMCOMP,
  V_DETALLE_AUXILIAR_CNT.TIPO_CPTE TIPOCOMP,
 V_DETALLE_AUXILIAR_CNT.VLRAGIRAR  VLRAGIRAR,
  PCK_SYSMAN_UTL.FC_VALOR_LETRAS(V_DETALLE_AUXILIAR_CNT.VLRAGIRAR) VLRAGIRARL,
  V_DETALLE_AUXILIAR_CNT.VLR_DOCUMENTO   VLR_DOCUMENTO ,
  CASE
    WHEN V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO IS NULL
    THEN ' '
    ELSE V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO
  END
  || ' / '
  || V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO_NOM CENTRO,
  V_DETALLE_AUXILIAR_CNT.VALOR_DEBITO VALOR_DEBITO,
  V_DETALLE_AUXILIAR_CNT.VALOR_CREDITO VALOR_CREDITO,
  V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA  CODIGO_CUENTA,
  V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE TOTALNOMBRE,
  CASE
    WHEN V_DETALLE_AUXILIAR_CNT.VALOR_DEBITO <> 0
    THEN 'A '
      || PCK_SYSMAN_UTL.FC_CODIGO_CNT (V_DETALLE_AUXILIAR_CNT.COMPANIA, V_DETALLE_AUXILIAR_CNT.ANO,V_DETALLE_AUXILIAR_CNT.CUENTA_COMP, V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO, V_DETALLE_AUXILIAR_CNT.TERCERO,V_DETALLE_AUXILIAR_CNT.SUCURSAL, V_DETALLE_AUXILIAR_CNT.AUXILIAR, V_DETALLE_AUXILIAR_CNT.REFERENCIA, V_DETALLE_AUXILIAR_CNT.FUENTE_RECURSOS)
