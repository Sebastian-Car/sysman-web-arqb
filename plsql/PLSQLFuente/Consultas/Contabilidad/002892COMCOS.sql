MERGE INTO CONSULTAS FIN USING (SELECT '002892COMCOS' INFORME ,TO_CLOB(q'[SELECT
  VDT.COMPANIA AS COMP
  , VDT.TIPO_CPTE AS TIPOCOMP
  , VDT.COMPROBANTE AS NUMCOMP
  , VDT.COMPROBANTE AS NUMERO
  , VDT.TERCERO_COM_NOM AS NOMBRE
  , VDT.TERCERO_COM  AS TERCERO
  , VDT.DIRECCION_DET AS DIRECCION1
  , VDT.TELEFONOS AS TELEFONOS1
  , CASE
    WHEN TO_NUMBER(VDT.VLRAGIRAR) = 1
    THEN 0
    ELSE VDT.VLRAGIRAR
  END SUMADE
  , TO_CHAR(VDT.FECHA,'DD/MM/YYYY') FECHA
  , VDT.CENTRO_COSTO
  , VDT.VLR_DOCUMENTO
  , VDT.CONTRIBUYENTE
  , VDT.DESCRIPCION
  , VDT.TEXTO
  , VDT.CUENTAPPTAL
  , VDT.CODIGO_CUENTA_DET AS CODIGO_CUENTA
  , VDT.CUENTANOMBRE AS TOTALNOMBRE
  , VDT.VALOR_DEBITO
  , VDT.VALOR_CREDITO
  , VDT.NRO_DOCUMENTO
  , VDT.NRO_DOCUMENTO_DET AS NRO_DOCUMENTO_DCNT
  , VDT.FECHA_VCN_DOC
  , VDT.VLRAGIRAR
  , CASE WHEN VALOR_DEBITO <> 0 THEN 'A' || CODIGO_CUENTA ELSE 'Z' || CODIGO_CUENTA END SEC
  , VDT.CREATED_BY AS ELABORO
  , A.CUENTA    CUENTA_TERCERO
  , A.NOM_BANCO   NOM_BANCO
  , A.NOMBREBANCO  NOMBRE_BANCO
  , VDT.CENTRO_COSTO AS CENTRO
  , VDT.ANO ANOCOMP
  , VDT.CENTRO_COSTO_VIS || '-' || CENTRO_COSTO.NOMBRE CENTRO_VIS
FROM V_DETALLE_AUXILIAR_CNT VDT

LEFT JOIN ( SELECT TERCEROPAGOS.COMPANIA,TERCEROPAGOS.NIT,TERCEROPAGOS.SUCURSAL,MAX(TERCEROPAGOS.CUENTA)  KEEP (DENSE_RANK LAST ORDER BY ROWNUM) CUENTA,
            MAX(TERCEROPAGOS.BANCO)  KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NOM_BANCO,MAX(BANCO.NOMBREBANCO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NOMBREBANCO
            FROM TERCEROPAGOS
                LEFT JOIN BANCO
                    ON TERCEROPAGOS.COMPANIA  = BANCO.COMPANIA
                    AND TERCEROPAGOS.BANCO  = BANCO.BANCO
            GROUP BY TERCEROPAGOS.COMPANIA,TERCEROPAGOS.NIT,TERCEROPAGOS.SUCURSAL
) A ON VDT.COMPANIA  = A.COMPANIA
AND VDT.TERCERO  = A.NIT
AND VDT.SUCURSAL = A.SUCURSAL
LEFT JOIN CENTRO_COSTO
ON VDT.COMPANIA = CENTRO_COSTO.COMPANIA
AND VDT.ANO = CENTRO_COSTO.ANO
AND VDT.CENTRO_COSTO_VIS = CENTRO_COSTO.CODIGO
WHERE VDT.COMPANIA = s$compania$s
AND VDT.ANO = s$ano$s
AND VDT.TIPO_CPTE = 's$tipoCpte$s'
AND VDT.COMPROBANTE BETWEEN  s$numeroPptoInicial$s AND s$numeroPptoFinal$s
ORDER BY (A.CUENTA)]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, 'calarcon' CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
