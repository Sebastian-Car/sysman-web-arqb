MERGE INTO CONSULTAS FIN USING (SELECT '000654LisAuxCtaCentro' INFORME ,TO_CLOB(q'[SELECT COMPROBANTE_CNT.COMPANIA,
       COMPROBANTE_CNT.ANO,
       PLAN_CONTABLE.CODIGO CODIGO_CUENTA,
       CENTRO_COSTO.CODIGO CENTRO_COSTO,
       CENTRO_COSTO.NOMBRE CENTRO_COSTO_NOM,
       PLAN_CONTABLE.NOMBRE CUENTANOMBRE,
       SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO)  AS SUMADEBITO,
       SUM(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO) AS SUMACREDITO,
       SUM(DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE) AS SUMABASE
FROM COMPROBANTE_CNT
 INNER JOIN DETALLE_COMPROBANTE_CNT
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA    = COMPROBANTE_CNT.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.ANO         = COMPROBANTE_CNT.ANO
    AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE   = COMPROBANTE_CNT.TIPO
    AND DETALLE_COMPROBANTE_CNT.COMPROBANTE = COMPROBANTE_CNT.NUMERO
  INNER JOIN PLAN_CONTABLE
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.ANO      = PLAN_CONTABLE.ANO
    AND DETALLE_COMPROBANTE_CNT.CUENTA   = PLAN_CONTABLE.CODIGO
  INNER JOIN CENTRO_COSTO
   ON  DETALLE_COMPROBANTE_CNT.COMPANIA  = CENTRO_COSTO.COMPANIA
   AND DETALLE_COMPROBANTE_CNT.ANO       = CENTRO_COSTO.ANO
   AND DETALLE_COMPROBANTE_CNT.CENTRO_COSTO = CENTRO_COSTO.CODIGO
  WHERE DETALLE_COMPROBANTE_CNT.COMPANIA = s$compania$s
  AND DETALLE_COMPROBANTE_CNT.ANO      = s$anoTrabajo$s
  AND DETALLE_COMPROBANTE_CNT.CENTRO_COSTO BETWEEN 's$centroInicial$s' AND 's$centroFinal$s'
  AND DETALLE_COMPROBANTE_CNT.CUENTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
  AND DETALLE_COMPROBANTE_CNT.FECHA BETWEEN s$fechaInicial$s 
                                       AND s$fechaFinal$s
  GROUP BY COMPROBANTE_CNT.COMPANIA,
         COMPROBANTE_CNT.ANO,
         PLAN_CONTABLE.CODIGO,
         CENTRO_COSTO.CODIGO,
         CENTRO_COSTO.NOMBRE,
         PLAN_CONTABLE.NOMBRE
]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
