SELECT  
V_PLAN_CONTABLE.COMPANIA, 
SUBSTR(V_PLAN_CONTABLE.CODIGO,1,1) AS CLASE,  
SUM((CASE WHEN (V_PLAN_CONTABLE.NATURALEZA='D' AND V_PLAN_CONTABLE.SALDOs$mestrabajo$s>=0) THEN V_PLAN_CONTABLE.SALDOs$mestrabajo$s ELSE 0 END)+ (CASE WHEN (V_PLAN_CONTABLE.NATURALEZA='C' AND V_PLAN_CONTABLE.SALDOs$mestrabajo$s<0) THEN -V_PLAN_CONTABLE.SALDOs$mestrabajo$s ELSE 0 END)) AS SALDONUEDEBITO,   
SUM((CASE WHEN (V_PLAN_CONTABLE.NATURALEZA='C' AND V_PLAN_CONTABLE.SALDOs$mestrabajo$s>=0) THEN V_PLAN_CONTABLE.SALDOs$mestrabajo$s ELSE 0 END)+ (CASE WHEN (V_PLAN_CONTABLE.NATURALEZA='D' AND V_PLAN_CONTABLE.SALDOs$mestrabajo$s<0) THEN -V_PLAN_CONTABLE.SALDOs$mestrabajo$s ELSE 0 END)) AS SALDONUECREDITO,   
CLASE.NOMBRE AS NOMBRECLASE 
FROM V_PLAN_CONTABLE  
INNER JOIN V_PLAN_CONTABLE CLASE ON V_PLAN_CONTABLE.COMPANIA     = CLASE.COMPANIA 
                      AND V_PLAN_CONTABLE.ANO          = CLASE.ANO 
                      AND SUBSTR(V_PLAN_CONTABLE.CODIGO,1,1) =CLASE.CODIGO                                 
WHERE V_PLAN_CONTABLE.COMPANIA = s$compania$s  
AND V_PLAN_CONTABLE.ANO =  s$anotrabajo$s 
AND V_PLAN_CONTABLE.CODIGO >= 's$codInicial$s'  
AND V_PLAN_CONTABLE.CODIGO <= 's$codFinal$s'   
AND LENGTH(V_PLAN_CONTABLE.CODIGO)<= s$digitos$s 
AND (V_PLAN_CONTABLE.MOVIMIENTO <> 0  
		OR V_PLAN_CONTABLE.MAN_AUX_TER <> 0 
		OR V_PLAN_CONTABLE.MAN_AUX_GEN <> 0  
		OR V_PLAN_CONTABLE.MAN_CEN_CTO <> 0 
    OR V_PLAN_CONTABLE.MAN_AUX_REF <> 0) 
s$condicionSub$s 
AND V_PLAN_CONTABLE.AUXILIAR_INFORME IS NULL 
AND V_PLAN_CONTABLE.CENTRO_COSTO_INFORME IS NULL 
AND V_PLAN_CONTABLE.TERCERO_INFORME IS NULL 
AND V_PLAN_CONTABLE.REFERENCIA_INFORME IS NULL 
GROUP BY V_PLAN_CONTABLE.COMPANIA, SUBSTR(V_PLAN_CONTABLE.CODIGO,1,1), CLASE.NOMBRE 
ORDER BY CLASE
