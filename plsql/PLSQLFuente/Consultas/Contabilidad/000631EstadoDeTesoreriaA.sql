SELECT SUBSTR(SALDOAUX.CODIGO,0,1) CLASE ,
  SALDOAUX.CODIGO ,
  SALDOAUX.NOMBRE ,
  CUENTABANCOS.CUENTANUMERO ,
  SALDOAUX.SALDOs$mesI$s SALDOBASE,
  SALDOAUX.DEBITO1  + SALDOAUX.DEBITO2 + SALDOAUX.DEBITO3 + SALDOAUX.DEBITO4 + SALDOAUX.DEBITO5 + SALDOAUX.DEBITO6 + SALDOAUX.DEBITO7 + SALDOAUX.DEBITO8 + SALDOAUX.DEBITO9 + SALDOAUX.DEBITO10 + SALDOAUX.DEBITO11 + SALDOAUX.DEBITO12 SUMADEDEBITO,
  SALDOAUX.CREDITO1 + SALDOAUX.CREDITO2 + SALDOAUX.CREDITO3 + SALDOAUX.CREDITO4 + SALDOAUX.CREDITO5 + SALDOAUX.CREDITO6 + SALDOAUX.CREDITO7 + SALDOAUX.CREDITO8 + SALDOAUX.CREDITO9 + SALDOAUX.CREDITO10 + SALDOAUX.CREDITO11 + SALDOAUX.CREDITO12 SUMADECREDITO,
  (SALDOAUX.DEBITO1 + SALDOAUX.DEBITO2 + SALDOAUX.DEBITO3 + SALDOAUX.DEBITO4 + SALDOAUX.DEBITO5 + SALDOAUX.DEBITO6 + SALDOAUX.DEBITO7 + SALDOAUX.DEBITO8 + SALDOAUX.DEBITO9 + SALDOAUX.DEBITO10 + SALDOAUX.DEBITO11 + SALDOAUX.DEBITO12)- (SALDOAUX.DEBITO1 + SALDOAUX.DEBITO2 + SALDOAUX.DEBITO3 + SALDOAUX.DEBITO4 + SALDOAUX.DEBITO5 + SALDOAUX.DEBITO6 + SALDOAUX.DEBITO7 + SALDOAUX.DEBITO8 + SALDOAUX.DEBITO9 + SALDOAUX.DEBITO10 + SALDOAUX.DEBITO11 + SALDOAUX.DEBITO12) - (SALDOAUX.CREDITO1 + SALDOAUX.CREDITO2 + SALDOAUX.CREDITO3 + SALDOAUX.CREDITO4 + SALDOAUX.CREDITO5 + SALDOAUX.CREDITO6 + SALDOAUX.CREDITO7 + SALDOAUX.CREDITO8 + SALDOAUX.CREDITO9 + SALDOAUX.CREDITO10 + SALDOAUX.CREDITO11 + SALDOAUX.CREDITO12) NUEVOSALDO,
  SALDOAUX.AJUSTEs$mesFinal$s SALDOEXTRACTO ,
  SUBSTR(
  CASE
    WHEN SUBSTR(SALDOAUX.CODIGO,0,1) IN('0')
    THEN 'Z'
      || SALDOAUX.CODIGO
    ELSE SALDOAUX.CODIGO
  END ,0 ,1) CLASEORDEN ,
  SALDOAUX.COMPANIA ,
  SALDOAUX.ANO ,
  SALDOAUX.CODIGO ID ,
  SALDOAUX.TERCERO ,
  SALDOAUX.AUXILIAR ,
  SALDOAUX.CENTRO_COSTO ,
  CASE
    WHEN LPAD( SALDOAUX.CODIGO,1) IN('0')
    THEN 'Z'
      || SALDOAUX.CODIGO
    ELSE SALDOAUX.CODIGO
  END ORDEN ,
  SALDOAUX.NATURALEZA ,
  SALDOAUX.SALDO12 NSALDO  
FROM V_PLAN_CONTABLE SALDOAUX
LEFT JOIN CUENTABANCOS
ON SALDOAUX.COMPANIA          = CUENTABANCOS.COMPANIA
AND SALDOAUX.ANO              = CUENTABANCOS.ANO
AND SALDOAUX.CODIGO           = CUENTABANCOS.IDCONTABLE
WHERE SALDOAUX.COMPANIA = s$compania$s
  AND SALDOAUX.ANO      = s$anio$s
  AND SALDOAUX.CODIGO   BETWEEN s$codigoInicial$s AND s$codigoFinal$s
  AND ( SALDOAUX.MAN_AUX_TER = CASE WHEN 1=s$manTer$s THEN -1 ELSE SALDOAUX.MAN_AUX_TER END  AND
        SALDOAUX.MAN_AUX_GEN = CASE WHEN 1=s$manAux$s THEN -1 ELSE SALDOAUX.MAN_AUX_GEN END  AND
        SALDOAUX.MAN_CEN_CTO = CASE WHEN 1=s$manCen$s THEN -1 ELSE SALDOAUX.MAN_CEN_CTO END  AND
        SALDOAUX.MAN_AUX_REF = CASE WHEN 1=s$manRef$s THEN -1 ELSE SALDOAUX.MAN_AUX_REF END  AND
        SALDOAUX.MAN_AUX_FUE = CASE WHEN 1=s$manFue$s THEN -1 ELSE SALDOAUX.MAN_AUX_FUE END  
       )
  AND LENGTH(SALDOAUX.CODIGO)<= s$digitos$s 
s$saldoCero$s
ORDER BY CLASE,
  CODIGO
