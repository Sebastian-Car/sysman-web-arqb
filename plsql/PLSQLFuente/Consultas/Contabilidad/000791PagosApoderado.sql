SELECT TIPO_COMPROBANTE.NOMBRE || ' ' || COMPROBANTE_CNT.NUMERO AS COMPROBANTE, 
        TERCERO.NIT, 
        TERCERO.NOMBRE AS NOMBRETERCERO, 
        COMPROBANTE_CNT.FECHA, 
        COMPROBANTE_CNT.DESCRIPCION || ' ' || COMPROBANTE_CNT.TEXTO AS OBJETO, 
        COMPROBANTE_CNT.VLRAGIRAR,
        COMPROBANTE_CNT.PAGOAPODERADO, 
        TERCEROP.NIT NITAPODERADO,
        TERCEROP.NOMBRE AS NOMBREAPODERADO
FROM COMPROBANTE_CNT LEFT JOIN TERCEROPAGOS 
       ON COMPROBANTE_CNT.COMPANIA    = TERCEROPAGOS.COMPANIA 
      AND COMPROBANTE_CNT.CUENTABANCO = TERCEROPAGOS.CUENTA 
        LEFT JOIN TERCERO TERCEROP 
             ON TERCEROPAGOS.COMPANIA = TERCEROP.COMPANIA 
            AND TERCEROPAGOS.SUCURSAL = TERCEROP.SUCURSAL
            AND TERCEROPAGOS.NIT = TERCEROP.NIT
    LEFT JOIN TERCERO 
         ON COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA 
        AND COMPROBANTE_CNT.TERCERO  = TERCERO.NIT
        AND COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL
    LEFT JOIN TIPO_COMPROBANTE 
         ON COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA
        AND COMPROBANTE_CNT.TIPO     = TIPO_COMPROBANTE.CODIGO
WHERE COMPROBANTE_CNT.COMPANIA = s$compania$s
  AND TERCEROP.NIT             = 's$tercero$s'
  AND COMPROBANTE_CNT.FECHA BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY') 
  AND COMPROBANTE_CNT.PAGOAPODERADO NOT IN (0)
