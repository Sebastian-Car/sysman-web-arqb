SELECT
  PLAN_CONTABLE.COMPANIA,
  PLAN_CONTABLE.ANO,
  PLAN_CONTABLE.CODIGO CUENTA,
  SUM(SALDOS_AUX.VALOR_DEBITO) ENTRADAS, 
  SUM(SALDOS_AUX.VALOR_CREDITO) SALIDAS,
  PLAN_CONTABLE.NOMBRE, 
  MIN(SALDOS_AUX.NRO_DOCUMENTO) CHEQUEINI,
  MIN(SALDOS_AUX.NRO_DOCUMENTO) CHEQUEFIN,
  SUM(SALDOS_AUX.SALDO_ANT) SALDOANTERIOR,
  SUM(SALDOS_AUX.SALDO_ANT + SALDOS_AUX.SALDO) SALDOFINAL
FROM PLAN_CONTABLE INNER JOIN ( s$baseAux$s )
 SALDOS_AUX
ON SALDOS_AUX.COMPANIA                                        = PLAN_CONTABLE.COMPANIA
   AND SALDOS_AUX.ANO                                             = PLAN_CONTABLE.ANO
   AND SUBSTR(SALDOS_AUX.CODIGO_CUENTA, 1, LENGTH(PLAN_CONTABLE.CODIGO)) = PLAN_CONTABLE.CODIGO

GROUP BY  PLAN_CONTABLE.COMPANIA, 
          PLAN_CONTABLE.ANO, 
          PLAN_CONTABLE.CODIGO,
          PLAN_CONTABLE.NOMBRE
s$condicionSaldoCero$s
