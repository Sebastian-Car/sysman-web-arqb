WITH TMPBOLETIN AS (SELECT COMPANIA, ANO, TIPO_CPTE, COMPROBANTE 
 FROM DETALLE_COMPROBANTE_CNT 
 WHERE COMPANIA = s$compania$s
 AND EXTRACT (MONTH FROM FECHA) = EXTRACT (MONTH FROM TO_DATE('s$fecha$s')) 
 AND TO_CHAR(FECHA, 'DD/MM/YYYY') = 's$fecha$s'
 AND SUBSTR(CUENTA, 0, LENGTH('s$cuenta$s')) = 's$cuenta$s' 
 AND ANO = EXTRACT (YEAR FROM TO_DATE('s$fecha$s')) 
 GROUP BY COMPANIA, ANO, TIPO_CPTE, COMPROBANTE)
 SELECT 
  DETALLE_COMPROBANTE_CNT.COMPANIA, 
  DETALLE_COMPROBANTE_CNT.ANO, 
  DETALLE_COMPROBANTE_CNT.TIPO_CPTE, 
  DETALLE_COMPROBANTE_CNT.CUENTA,
  MAX(TIPO_COMPROBANTE.NOMBRE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) UNOMBRE, 
  MIN(TERCERO.NOMBRE) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) TERCERO,
  SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO) VALOR_DEBITO, 
  SUM(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO) VALOR_CREDITO
FROM DETALLE_COMPROBANTE_CNT
    INNER JOIN TERCERO
        ON TERCERO.COMPANIA = DETALLE_COMPROBANTE_CNT.COMPANIA 
        AND TERCERO.SUCURSAL = DETALLE_COMPROBANTE_CNT.SUCURSAL
        AND TERCERO.NIT = DETALLE_COMPROBANTE_CNT.TERCERO
    INNER JOIN TIPO_COMPROBANTE 
        ON DETALLE_COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA
        AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
    INNER JOIN PLAN_CONTABLE 
        ON DETALLE_COMPROBANTE_CNT.ANO = PLAN_CONTABLE.ANO
        AND DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA
        AND DETALLE_COMPROBANTE_CNT.CUENTA = PLAN_CONTABLE.CODIGO
  INNER JOIN TMPBOLETIN 
      ON TMPBOLETIN.COMPANIA = DETALLE_COMPROBANTE_CNT.COMPANIA
      AND TMPBOLETIN.ANO = DETALLE_COMPROBANTE_CNT.ANO
      AND TMPBOLETIN.TIPO_CPTE = DETALLE_COMPROBANTE_CNT.TIPO_CPTE 
      AND TMPBOLETIN.COMPROBANTE = DETALLE_COMPROBANTE_CNT.COMPROBANTE 
GROUP BY DETALLE_COMPROBANTE_CNT.COMPANIA, DETALLE_COMPROBANTE_CNT.ANO, DETALLE_COMPROBANTE_CNT.TIPO_CPTE, DETALLE_COMPROBANTE_CNT.CUENTA
ORDER BY DETALLE_COMPROBANTE_CNT.ANO, DETALLE_COMPROBANTE_CNT.TIPO_CPTE, MAX(DETALLE_COMPROBANTE_CNT.COMPROBANTE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM)
