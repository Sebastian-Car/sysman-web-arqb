SELECT
    V_DETALLE_AUXILIAR_CNT.COMPANIA            COMP,
    V_DETALLE_AUXILIAR_CNT.ANO                 ANOCOMP,
    V_DETALLE_AUXILIAR_CNT.TIPO_CPTE           TIPO,
    V_DETALLE_AUXILIAR_CNT.COMPROBANTE         NUMCOMP,
    V_DETALLE_AUXILIAR_CNT.COMPROBANTE,
    V_DETALLE_AUXILIAR_CNT.FECHA_COMPROBANTE   FECHA,
    V_DETALLE_AUXILIAR_CNT.TERCERO_COM_NOM     NOMBRETERCERO1,
    V_DETALLE_AUXILIAR_CNT.TERCERO_COM         TERCERO,
    V_DETALLE_AUXILIAR_CNT.DIRECCION           TERCERODIRE,
    V_DETALLE_AUXILIAR_CNT.TERCERO_COM_TEL     TELEFONOS,
    V_DETALLE_AUXILIAR_CNT.TERCERO_COM_POS     CODIGOPOSTAL,
    V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE        BANCO,
    V_DETALLE_AUXILIAR_CNT.NRO_DOCUMENTO,
    V_DETALLE_AUXILIAR_CNT.VLRAGIRAR,
    V_DETALLE_AUXILIAR_CNT.DESCRIPCION,
    V_DETALLE_AUXILIAR_CNT.TEXTO,
    V_DETALLE_AUXILIAR_CNT.CMPTE_AFECTADO_DET  CMPTE_AFECTADO,
    CODIGO_CUENTA_DET                          CODIGO_CUENTA,
    CUENTANOMBRE                               TOTALNOMBRE,
    V_DETALLE_AUXILIAR_CNT.VALOR_DEBITO,
    V_DETALLE_AUXILIAR_CNT.VALOR_CREDITO,
    V_DETALLE_AUXILIAR_CNT.CREATED_BY                 CREADOR,
    NVL(USUARIO.NOMBRE1,'')
    || ' '
    || NVL(USUARIO.NOMBRE2,'')
    || ' '
    || NVL(USUARIO.APELLIDO1,'')
    || ' '
    || NVL(USUARIO.APELLIDO2,'')NOMBRE_USUARIO
FROM
    V_DETALLE_AUXILIAR_CNT LEFT JOIN USUARIO ON V_DETALLE_AUXILIAR_CNT.CREATED_BY = USUARIO.CODIGO
WHERE
  V_DETALLE_AUXILIAR_CNT.COMPANIA    = s$compania$s
AND V_DETALLE_AUXILIAR_CNT.ANO       = s$ano$s
AND V_DETALLE_AUXILIAR_CNT.TIPO_CPTE = 's$tipoCpte$s'
AND V_DETALLE_AUXILIAR_CNT.COMPROBANTE BETWEEN 's$numeroPptoInicial$s' AND  's$numeroPptoFinal$s'