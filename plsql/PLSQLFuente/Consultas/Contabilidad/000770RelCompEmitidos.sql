SELECT TIPO_COMPROBANTE.NOMBRE,
    MIN(COMPROBANTE_CNT.NUMERO) AS MINDENUMERO,
    MAX(COMPROBANTE_CNT.NUMERO) AS ULTIMODENUMERO
FROM COMPROBANTE_CNT LEFT JOIN TIPO_COMPROBANTE ON 
    COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA 
    AND COMPROBANTE_CNT.TIPO = TIPO_COMPROBANTE.CODIGO
WHERE COMPROBANTE_CNT.COMPANIA = s$compania$s 
    AND TIPO_COMPROBANTE.CLASE_CONTABLE IN ('A', 'I', 'B', 'E', 'S', 'G', 'J')
    AND COMPROBANTE_CNT.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
GROUP BY TIPO_COMPROBANTE.NOMBRE,
  TIPO_COMPROBANTE.CLASE_CONTABLE
