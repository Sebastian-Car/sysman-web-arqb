MERGE INTO CONSULTAS FIN USING (SELECT '002511COM_GOBERNACION_sub' INFORME ,TO_CLOB(q'[SELECT  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE  NUMCOMP,
        DETALLE_COMPROBANTE_PPTAL.CUENTA,
        PLAN_PRESUPUESTAL.NOMBRE,
        SUM(CASE WHEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO IN(0) 
                 THEN DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO - DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO
                 ELSE DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO
            END) VALOR
FROM DETALLE_COMPROBANTE_PPTAL
  INNER JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA           = PLAN_PRESUPUESTAL.COMPANIA 
    AND DETALLE_COMPROBANTE_PPTAL.ANO               = PLAN_PRESUPUESTAL.ANO
    AND DETALLE_COMPROBANTE_PPTAL.ID                 = PLAN_PRESUPUESTAL.ID
  INNER JOIN AUXILIAR
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA   = AUXILIAR.COMPANIA 
    AND DETALLE_COMPROBANTE_PPTAL.ANO       = AUXILIAR.ANO  
    AND DETALLE_COMPROBANTE_PPTAL.AUXILIAR  = AUXILIAR.CODIGO     
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA    = $P{PR_COMPANIA}  
  AND DETALLE_COMPROBANTE_PPTAL.ANO         = $P{PR_ANO}
  AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = $P{PR_TIPO}
  AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE BETWEEN $P{PR_NUMERO} AND $P{PR_NUMERO} 
GROUP BY 
  DETALLE_COMPROBANTE_PPTAL.COMPANIA,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
  PLAN_PRESUPUESTAL.NOMBRE,
  DETALLE_COMPROBANTE_PPTAL.CUENTA]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
  
  MERGE INTO CONSULTAS_SUB FIN USING (SELECT '002510COM_GOBERNACION' INFORME_PADRE, '002511COM_GOBERNACION_sub' INFORME_HIJO, 'PR_STRSQL_INFORMESUBCOM_DC' PARAMETRO ,NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (FIN.INFORME_PADRE = INI.INFORME_PADRE AND INI.INFORME_HIJO = FIN.INFORME_HIJO)  WHEN MATCHED THEN  UPDATE SET FIN.PARAMETRO = INI.PARAMETRO , FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME_PADRE,INFORME_HIJO, PARAMETRO,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME_PADRE,INI.INFORME_HIJO, INI.PARAMETRO,INI.CREATED_BY,SYSDATE);