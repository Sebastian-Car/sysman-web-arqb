MERGE INTO CONSULTAS FIN USING (SELECT '002907SubCOMDCEGR' INFORME ,TO_CLOB(q'[SELECT DC.COMPANIA,
    DC.ANO,
    DC.TIPO_CPTE TIPO,
    DC.COMPROBANTE,
    DC.CUENTA CUENTACOMPLETA,
    DC2.CMPTE_AFECTADO NUMERODIS,
    DC2.COMPROBANTE NUMERORES,
    PLAN_PRESUPUESTAL.NOMBRE PRIMERODENOMBRE,
    DC1.CODIGO_CPC CPC_COD_DANE,
    SUM(DC.VALOR_DEBITO-DC.VALOR_CREDITO) AS VALORNETO
FROM DETALLE_COMPROBANTE_PPTAL DC
INNER JOIN DETALLE_COMPROBANTE_PPTAL DC1
ON DC.COMPANIA = DC1.COMPANIA
AND DC.ANO = DC1.ANO
AND DC.TIPO_CPTE_AFECT = DC1.TIPO_CPTE
AND DC.CMPTE_AFECTADO = DC1.COMPROBANTE
AND DC.CONSECUTIVOPPTO = DC1.CONSECUTIVO
INNER JOIN DETALLE_COMPROBANTE_PPTAL DC2
ON DC1.COMPANIA = DC2.COMPANIA
AND DC1.ANO = DC2.ANO
AND DC1.TIPO_CPTE_AFECT = DC2.TIPO_CPTE
AND DC1.CMPTE_AFECTADO = DC2.COMPROBANTE
AND DC1.CONSECUTIVOPPTO = DC2.CONSECUTIVO
INNER JOIN PLAN_PRESUPUESTAL
ON DC.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
AND DC.ANO = PLAN_PRESUPUESTAL.ANO
AND DC.CUENTA = PLAN_PRESUPUESTAL.CODIGO
WHERE DC.COMPANIA = $P{PR_COMPANIA}
    AND DC.ANO = $P{PR_ANO}
    AND DC.TIPO_CPTE = $P{PR_TIPO}
    AND DC.COMPROBANTE BETWEEN $P{PR_COMPROBANTE} AND $P{PR_COMPROBANTE}
GROUP BY DC.COMPANIA,
    DC.ANO,
    DC.TIPO_CPTE,
    DC.COMPROBANTE,
    DC.CUENTA,
    DC2.CMPTE_AFECTADO,
    DC2.COMPROBANTE,
    PLAN_PRESUPUESTAL.NOMBRE,
    DC1.CODIGO_CPC]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
	
