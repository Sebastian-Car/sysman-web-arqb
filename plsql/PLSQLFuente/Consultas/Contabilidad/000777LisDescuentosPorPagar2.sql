SELECT DETALLE_COMPROBANTE_CNT.CUENTA   CODIGO_CUENTA, 
       PLAN_CONTABLE.NOMBRE             PLAN_CONTABLE_NOM,
       PLAN_CONTABLE_1.NOMBRE           PLAN_CONTABL_1_NOM,
       DETALLE_COMPROBANTE_CNT.FECHA    DETALLE_COMP_CNT_FECHA,
       DETALLE_COMPROBANTE_CNT.TIPO_CPTE,
       DETALLE_COMPROBANTE_CNT.COMPROBANTE,
       TERCERO.NOMBRE                   TERCERO_NOM, 
       DETALLE_COMPROBANTE_CNT.TERCERO,
       DETALLE_COMPROBANTE_CNT.VALOR_DEBITO, 
	     DETALLE_COMPROBANTE_CNT.VALOR_CREDITO,
       COMPAFECTA.TIPO ||' / '|| COMPAFECTA.NUMERO DOCQUELOAFECTO,
       COMPAFECTA.FECHA                 COMPROBANTE_CNT_1_FECHA, 
       TRIM(COMPAFECTADOS.TEXTO)        TEXTO,
       COMPAFECTADOS.VLRAGIRAR
FROM COMPROBANTE_CNTAFECTADOS
  INNER JOIN COMPROBANTE_CNT COMPAFECTADOS
    ON  COMPROBANTE_CNTAFECTADOS.COMPANIA             = COMPAFECTADOS.COMPANIA
    AND COMPROBANTE_CNTAFECTADOS.ANO                  = COMPAFECTADOS.ANO
    AND COMPROBANTE_CNTAFECTADOS.TIPO_CPTE_AFECT      = COMPAFECTADOS.TIPO
    AND COMPROBANTE_CNTAFECTADOS.COMPROBANTE_AFECT    = COMPAFECTADOS.NUMERO
  LEFT JOIN PLAN_CONTABLE PLAN_CONTABLE_1 
    ON  COMPAFECTADOS.COMPANIA = PLAN_CONTABLE_1.COMPANIA 
	  AND COMPAFECTADOS.ANO      = PLAN_CONTABLE_1.ANO 
	  AND COMPAFECTADOS.CUENTA   = PLAN_CONTABLE_1.CODIGO
     INNER JOIN DETALLE_COMPROBANTE_CNT 
  	  ON  COMPAFECTADOS.COMPANIA = DETALLE_COMPROBANTE_CNT.COMPANIA 
	    AND COMPAFECTADOS.TIPO     = DETALLE_COMPROBANTE_CNT.TIPO_CPTE 
	    AND COMPAFECTADOS.NUMERO   = DETALLE_COMPROBANTE_CNT.COMPROBANTE
 INNER JOIN PLAN_CONTABLE
    ON  COMPAFECTADOS.COMPANIA = PLAN_CONTABLE.COMPANIA
    AND COMPAFECTADOS.ANO      = PLAN_CONTABLE.ANO
    AND COMPAFECTADOS.CUENTA   = PLAN_CONTABLE.CODIGO
 LEFT JOIN TERCERO 
	ON  DETALLE_COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA 
	AND DETALLE_COMPROBANTE_CNT.TERCERO  = TERCERO.NIT 
	AND DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL 
 LEFT JOIN COMPROBANTE_CNT COMPAFECTA
     ON  COMPAFECTA.COMPANIA  = COMPROBANTE_CNTAFECTADOS.COMPANIA
    AND COMPAFECTA.ANO       = COMPROBANTE_CNTAFECTADOS.ANO
    AND COMPAFECTA.TIPO      = COMPROBANTE_CNTAFECTADOS.TIPO_CPTE
    AND COMPAFECTA.NUMERO    = COMPROBANTE_CNTAFECTADOS.COMPROBANTE
  INNER JOIN PLAN_CONTABLE PLANAFECTA
     ON  COMPAFECTA.COMPANIA = PLANAFECTA.COMPANIA
    AND COMPAFECTA.ANO      = PLANAFECTA.ANO
    AND COMPAFECTA.CUENTA   = PLANAFECTA.CODIGO 
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA = s$compania$s
  AND COMPAFECTADOS.TIPO BETWEEN 's$tipoInicial$s' AND 's$tipoFinal$s' 
  AND COMPAFECTADOS.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s  
  AND DETALLE_COMPROBANTE_CNT.CUENTA BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
