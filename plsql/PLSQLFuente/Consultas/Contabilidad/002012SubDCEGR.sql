SELECT DISTINCT DC.COMPANIA COMP,
DC.ANO ANOCOMP,
DC.COMPROBANTE NUMCOMP,
DS_PPTAL.CMPTE_AFECTADO NUMERODIS,
DC_PPTAL.CMPTE_AFECTADO NUMERORES,
DC.CUENTA           CUENTACOMPLETA,
FIRST_VALUE(PLAN_PRESUPUESTAL.NOMBRE) OVER(ORDER BY PLAN_PRESUPUESTAL.NOMBRE) || '/' ||TERCERO.NOMBRE ||
CASE WHEN PLAN_PRESUPUESTAL.MAN_AUX_GEN NOT IN (0) OR AUXILIAR.CODIGO = '99999999999999999999' THEN ' ' ELSE '/' || AUXILIAR.NOMBRE END ||
CASE WHEN PLAN_PRESUPUESTAL.MAN_CEN_CTO NOT IN (0) OR CENTRO_COSTO.CODIGO = '99999999999999999999' THEN ' ' ELSE '/' || CENTRO_COSTO.NOMBRE END ||
CASE WHEN PLAN_PRESUPUESTAL.MAN_AUX_REF NOT IN (0) OR REFERENCIA.CODIGO = '99999999999999999999' THEN ' ' ELSE '/' || REFERENCIA.NOMBRE END ||
CASE WHEN PLAN_PRESUPUESTAL.MAN_AUX_FUE NOT IN (0) OR FUENTE_RECURSOS.CODIGO = '99999999999999999999' THEN ' ' ELSE '/' || FUENTE_RECURSOS.NOMBRE END  PRIMERODENOMBRE,
SUM(DC.VALOR_DEBITO-DC.VALOR_CREDITO) VALORNETO
FROM DETALLE_COMPROBANTE_PPTAL DC
INNER JOIN PLAN_PRESUPUESTAL
ON DC.COMPANIA     = PLAN_PRESUPUESTAL.COMPANIA
AND DC.CUENTA        = PLAN_PRESUPUESTAL.CODIGO
AND DC.ANO          = PLAN_PRESUPUESTAL.ANO
INNER JOIN TERCERO
ON TERCERO.COMPANIA = DC.COMPANIA
AND TERCERO.NIT = DC.TERCERO
AND TERCERO.SUCURSAL = DC.SUCURSAL
INNER JOIN AUXILIAR
ON AUXILIAR.COMPANIA = DC.COMPANIA
AND AUXILIAR.ANO = DC.ANO
AND AUXILIAR.CODIGO = DC.AUXILIAR
INNER JOIN CENTRO_COSTO
ON CENTRO_COSTO.COMPANIA = DC.COMPANIA
AND CENTRO_COSTO.ANO = DC.ANO
AND CENTRO_COSTO.CODIGO = DC.AUXILIAR
INNER JOIN REFERENCIA
ON REFERENCIA.COMPANIA = DC.COMPANIA
AND REFERENCIA.ANO = DC.ANO
AND REFERENCIA.CODIGO = DC.AUXILIAR
INNER JOIN FUENTE_RECURSOS
ON FUENTE_RECURSOS.COMPANIA = DC.COMPANIA
AND FUENTE_RECURSOS.ANO = DC.ANO
AND FUENTE_RECURSOS.CODIGO = DC.AUXILIAR
INNER JOIN DETALLE_COMPROBANTE_PPTAL DC_PPTAL
ON DC_PPTAL.COMPANIA = DC.COMPANIA 
AND DC_PPTAL.ANO = DC.ANO_AFECT 
AND DC_PPTAL.TIPO_CPTE = DC.TIPO_CPTE_AFECT 
AND DC_PPTAL.COMPROBANTE = DC.CMPTE_AFECTADO 
INNER JOIN DETALLE_COMPROBANTE_PPTAL DS_PPTAL
ON DS_PPTAL.COMPANIA = DC_PPTAL.COMPANIA
AND DS_PPTAL.ANO = DC_PPTAL.ANO_AFECT
AND DS_PPTAL.TIPO_CPTE = DC_PPTAL.TIPO_CPTE_AFECT
AND DS_PPTAL.COMPROBANTE = DC_PPTAL.CMPTE_AFECTADO
WHERE DC.ANO        = s$ano$s
AND DC.TIPO_CPTE  = 's$tipoCpte$s'
AND DC.COMPROBANTE BETWEEN '$P!{PR_NUMCOMP}' AND '$P!{PR_NUMCOMP}'
GROUP BY DC.COMPANIA, DC.ANO, DC.COMPROBANTE, DC.CMPTE_AFECTADO, DC.COMPROBANTE, 
DC.CUENTA,DS_PPTAL.CMPTE_AFECTADO, DC_PPTAL.CMPTE_AFECTADO, PLAN_PRESUPUESTAL.NOMBRE, PLAN_PRESUPUESTAL.CODIGO, PLAN_PRESUPUESTAL.NOMBRE ,
TERCERO.NOMBRE ,AUXILIAR.NOMBRE,CENTRO_COSTO.NOMBRE,REFERENCIA.NOMBRE,FUENTE_RECURSOS.NOMBRE,
AUXILIAR.CODIGO,PLAN_PRESUPUESTAL.MAN_AUX_GEN,PLAN_PRESUPUESTAL.MAN_CEN_CTO,PLAN_PRESUPUESTAL.MAN_AUX_REF,
PLAN_PRESUPUESTAL.MAN_AUX_FUE,CENTRO_COSTO.CODIGO,REFERENCIA.CODIGO,FUENTE_RECURSOS.CODIGO