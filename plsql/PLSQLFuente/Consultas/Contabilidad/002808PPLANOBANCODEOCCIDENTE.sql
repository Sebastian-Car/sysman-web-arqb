MERGE INTO CONSULTAS FIN USING (SELECT '002808PPLANOBANCODEOCCIDENTE' INFORME ,TO_CLOB(q'[SELECT COUNT(*) OVER () AS TOTAL_REGISTROS,  
       SUM(VALORTRANSACCION) OVER () AS TOTALVALOR,
       CUENTAORIGEN,
       ANOPAGO,
       MESPAGO,
       DIAPAGO,
       NOMBREBENEFICIARIO,
       NIT,
       COMPROBANTE,
       FORMAPAGO,
       VALORTRANSACCION,
       TEXTO, 
       BANCODESTINO, 
       CUENTADESTINO, 
       TIPOCUENTA
FROM (
SELECT  CUENTABANCOS.CUENTANUMERO CUENTAORIGEN,
        EXTRACT(YEAR FROM COMPROBANTE_CNT.FECHA_VCN_DOC) ANOPAGO,
        EXTRACT(MONTH FROM COMPROBANTE_CNT.FECHA_VCN_DOC) MESPAGO,
        EXTRACT(DAY FROM COMPROBANTE_CNT.FECHA_VCN_DOC) DIAPAGO,
        CASE
          WHEN NVL(COMPROBANTE_CNT.PAGOAPODERADO,0) NOT IN (0)
          THEN APODERADO.NOMBRE
          ELSE TERCERO.NOMBRE
        END AS NOMBREBENEFICIARIO,
        CASE
          WHEN NVL(COMPROBANTE_CNT.PAGOAPODERADO,0) NOT IN (0)
          THEN APODERADO.NIT
          ELSE TERCERO.NIT
        END AS NIT,
        COMPROBANTE_CNT.NUMERO  COMPROBANTE,
        CASE
          WHEN NVL(PAGO_EFECTIVO,0) <> 0
          THEN '4'
          ELSE
            CASE
              WHEN NRO_DOCUMENTO IS NOT NULL
              THEN '1'
              ELSE
                CASE
                  WHEN COMPROBANTE_CNT.BANCO IN ('023')
                  THEN '2'
                  ELSE '3'
                END
            END
        END AS FORMAPAGO,
        COMPROBANTE_CNT.VLRAGIRAR  VALORTRANSACCION,
        COMPROBANTE_CNT.TEXTO, 
        COMPROBANTE_CNT.BANCO  BANCODESTINO, 
        COMPROBANTE_CNT.CUENTABANCO  CUENTADESTINO, 
        TERCEROPAGOS.TIPOCUENTA
      FROM CUENTABANCOS 
      LEFT JOIN ((COMPROBANTE_CNT
      LEFT JOIN TERCERO
      ON COMPROBANTE_CNT.COMPANIA  = TERCERO.COMPANIA
      AND COMPROBANTE_CNT.TERCERO  = TERCERO.NIT
      AND COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL
      LEFT JOIN TERCERO APODERADO
      ON TERCERO.COMPANIA            = APODERADO.COMPANIA
      AND TERCERO.NITAPODERADO       = APODERADO.NIT
      AND TERCERO.SUCURSALAPODERADO  = APODERADO.SUCURSAL)
      LEFT JOIN TERCEROPAGOS
      ON COMPROBANTE_CNT.TERCERO      = TERCEROPAGOS.NIT
      AND COMPROBANTE_CNT.SUCURSAL    = TERCEROPAGOS.SUCURSAL
      AND COMPROBANTE_CNT.COMPANIA    = TERCEROPAGOS.COMPANIA
      AND COMPROBANTE_CNT.BANCO       = TERCEROPAGOS.BANCO
      AND COMPROBANTE_CNT.CUENTABANCO = TERCEROPAGOS.CUENTA)
      ON CUENTABANCOS.IDCONTABLE = COMPROBANTE_CNT.CUENTA 
      AND CUENTABANCOS.ANO = COMPROBANTE_CNT.ANO 
      AND CUENTABANCOS.COMPANIA = COMPROBANTE_CNT.COMPANIA
      WHERE COMPROBANTE_CNT.COMPANIA = s$compania$s
      AND COMPROBANTE_CNT.ANO        = s$ano$s
      AND COMPROBANTE_CNT.TIPO       = 's$tipoEgreso$s' 
      AND COMPROBANTE_CNT.CUENTA BETWEEN 's$cuentaInicial$s'  AND 's$cuentaFinal$s'
      AND COMPROBANTE_CNT.NUMERO BETWEEN 's$egresoInicial$s' AND 's$egresoFinal$s'
      AND COMPROBANTE_CNT.TERCERO Between 's$terceroInicial$s' AND 's$terceroFinal$s'
      AND COMPROBANTE_CNT.PAGOENPLANO NOT IN (0)
      AND COMPROBANTE_CNT.ENV]') || TO_CLOB(q'[IADO         IN (0)
      ORDER BY COMPROBANTE_CNT.COMPANIA, COMPROBANTE_CNT.ANO, COMPROBANTE_CNT.TIPO)]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, 'MPEREZ' CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);