SELECT   
        DETALLE_COMPROBANTE_CNT.COMPANIA,  
        DETALLE_COMPROBANTE_CNT.TIPO_CPTE AS TIPO,  
        DETALLE_COMPROBANTE_CNT.COMPROBANTE AS NUMERO,  
        DETALLE_COMPROBANTE_CNT.FECHA,  
        DETALLE_COMPROBANTE_CNT.DESCRIPCION,  
        TERCERO.NOMBRE,  
        ABS(SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO-DETALLE_COMPROBANTE_CNT.VALOR_CREDITO)) AS VLR_DOCUMENTO,  
        ABS(SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO-DETALLE_COMPROBANTE_CNT.VALOR_CREDITO)) AS VLRAGIRAR, 
        TIPO_COMPROBANTE.CLASE_CONTABLE 
FROM PLAN_CONTABLE V_PLAN_CONTABLE  
  INNER JOIN DETALLE_COMPROBANTE_CNT  
    ON V_PLAN_CONTABLE.COMPANIA      = DETALLE_COMPROBANTE_CNT.COMPANIA  
    AND V_PLAN_CONTABLE.ANO          = DETALLE_COMPROBANTE_CNT.ANO  
    AND V_PLAN_CONTABLE.CODIGO          = DETALLE_COMPROBANTE_CNT.CUENTA 
  INNER JOIN TIPO_COMPROBANTE  
    ON DETALLE_COMPROBANTE_CNT.COMPANIA   = TIPO_COMPROBANTE.COMPANIA  
    AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO  
  INNER JOIN TERCERO   
    ON DETALLE_COMPROBANTE_CNT.TERCERO   = TERCERO.NIT  
    AND DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL  
    AND DETALLE_COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA  
WHERE TIPO_COMPROBANTE.CLASE_CONTABLE IN ('E','G')  
AND DETALLE_COMPROBANTE_CNT.COMPANIA= s$compania$s 
AND DETALLE_COMPROBANTE_CNT.CMPTE_AFECTADO = s$comprobante$s  
AND V_PLAN_CONTABLE.CLASECUENTA IN ('P','E')  
AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE_AFECT= s$tipo$s     
GROUP BY  TERCERO.NOMBRE,  
          TIPO_COMPROBANTE.CLASE_CONTABLE,  
          DETALLE_COMPROBANTE_CNT.COMPANIA, 
          DETALLE_COMPROBANTE_CNT.TIPO_CPTE, 
          DETALLE_COMPROBANTE_CNT.COMPROBANTE, 
          DETALLE_COMPROBANTE_CNT.FECHA, 
          DETALLE_COMPROBANTE_CNT.DESCRIPCION,  
          DETALLE_COMPROBANTE_CNT.CMPTE_AFECTADO, 
          DETALLE_COMPROBANTE_CNT.TIPO_CPTE_AFECT, 
          V_PLAN_CONTABLE.CLASECUENTA
