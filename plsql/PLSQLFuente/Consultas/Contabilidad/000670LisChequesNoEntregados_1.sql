SELECT  
  COMPANIA, 
  ANO, 
  TIPO_CPTE, 
  TIPO_COMPROBANTE_NOM NOMBRE_TIPO, 
  CLASE_CONTABLE, 
  COMPROBANTE, 
  CODIGO_CUENTA CUENTA, 
  CUENTANOMBRE NOMBRE_PLAN, 
  TERCERO_NOM NOMBRE_TERCERO, 
  TERCERO_DET TERCERO, 
  SUCURSAL_DET SUCURSAL, 
  CASE WHEN NRO_DOCUMENTO_DET IS NULL THEN NRO_DOCUMENTO ELSE NRO_DOCUMENTO_DET END NODOC, 
  CASE WHEN DESCRIPCION_DET IS NULL THEN DESCRIPCION ELSE DESCRIPCION_DET END DESCRIP, 
  VALOR_DEBITO, 
  VALOR_CREDITO, 
  TO_CHAR(FECHA, 'DD/MM/YYYY') FECHA,  
  CLASECUENTA, 
  ANULADO, 
  CASE WHEN CLASE_CONTABLE = 'E' THEN VALOR_CREDITO ELSE VALOR_DEBITO*-1 END TOTAL 
FROM V_DETALLE_AUXILIAR_CNT 
WHERE COMPANIA = s$compania$s 
  AND ANO = s$anio$s 
  AND CLASE_CONTABLE IN ('E') 
  AND CODIGO_CUENTA BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'  
  AND CLASECUENTA IN 'B' 
  AND ENTREGADO <> 0 
  AND FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
