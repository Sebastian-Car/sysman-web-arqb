SELECT
    DETALLE_COMPROBANTE_PPTAL.FECHA,
    DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
    DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
    PLAN_PRESUPUESTAL.CODIGO CODIGO_CUENTA,
    PLAN_PRESUPUESTAL.NOMBRE,
    DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO,
    DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO,
    DETALLE_COMPROBANTE_PPTAL.SUCURSAL,
    DETALLE_COMPROBANTE_PPTAL.TERCERO,
    TERCERO.NOMBRE NOMBRECOMPLETO,
    CASE 
        WHEN DETALLE_COMPROBANTE_PPTAL.AUXILIAR =  PCK_DATOS.FC_CONS_AUXILIAR    
        THEN ' '
        ELSE DETALLE_COMPROBANTE_PPTAL.AUXILIAR
        END CODAUXILIAR,
    CASE
        WHEN DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO = PCK_DATOS.FC_CONS_CENTRO
        THEN ' '
        ELSE DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO
        END CODCENTROCOSTO   
FROM   DETALLE_COMPROBANTE_PPTAL
    INNER JOIN COMPROBANTE_PPTAL 
        ON  COMPROBANTE_PPTAL.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA 
        AND  COMPROBANTE_PPTAL.ANO = DETALLE_COMPROBANTE_PPTAL.ANO 
        AND  COMPROBANTE_PPTAL.TIPO = DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE 
        AND  COMPROBANTE_PPTAL.NUMERO = DETALLE_COMPROBANTE_PPTAL.COMPROBANTE
    INNER JOIN PLAN_PRESUPUESTAL 
    ON PLAN_PRESUPUESTAL.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA
    AND PLAN_PRESUPUESTAL.ANO = DETALLE_COMPROBANTE_PPTAL.ANO 
    AND PLAN_PRESUPUESTAL.CODIGO =DETALLE_COMPROBANTE_PPTAL.CUENTA
    LEFT JOIN CENTRO_COSTO 
        ON  CENTRO_COSTO.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA
        AND CENTRO_COSTO.ANO  = DETALLE_COMPROBANTE_PPTAL.ANO
        AND  CENTRO_COSTO.CODIGO = DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO 
    LEFT JOIN AUXILIAR 
        ON AUXILIAR.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA
        AND AUXILIAR.ANO = DETALLE_COMPROBANTE_PPTAL.ANO
        AND AUXILIAR.CODIGO = DETALLE_COMPROBANTE_PPTAL.AUXILIAR
    LEFT JOIN TERCERO 
        ON TERCERO.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA  
        AND  TERCERO.NIT = DETALLE_COMPROBANTE_PPTAL.TERCERO  
        AND TERCERO.SUCURSAL = DETALLE_COMPROBANTE_PPTAL.SUCURSAL 
WHERE
    DETALLE_COMPROBANTE_PPTAL.COMPANIA =s$compania$s
  AND   PCK_SYSMAN_UTL.FC_ANIO(DETALLE_COMPROBANTE_PPTAL.FECHA) =s$ano$s
    AND   PCK_SYSMAN_UTL.FC_MES(DETALLE_COMPROBANTE_PPTAL.FECHA) BETWEEN s$fechaInicial$s AND s$fechaFinal$s
AND Detalle_comprobante_pptal.TERCERO = 's$terceroNit$s'
 AND Detalle_comprobante_pptal.SUCURSAL = 's$terceroSucursal$s'
ORDER BY
    DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
    DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
    DETALLE_COMPROBANTE_PPTAL.FECHA
