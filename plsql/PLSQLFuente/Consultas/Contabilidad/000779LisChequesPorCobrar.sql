SELECT COMPROBANTE_CNT.COMPANIA, 
COMPROBANTE_CNT.ANO, 
DETALLE_COMPROBANTE_CNT.TIPO_CPTE, 
TIPO_COMPROBANTE.NOMBRE TIPO_COMPROBANTE_NOMBRE, 
TIPO_COMPROBANTE.CLASE_CONTABLE, 
DETALLE_COMPROBANTE_CNT.COMPROBANTE, 
DETALLE_COMPROBANTE_CNT.CUENTA, 
PLAN_CONTABLE.NOMBRE PLAN_CONTABLE_NOMBRE, 
TERCERO.NOMBRE TERCERO_NOMBRE, 
DETALLE_COMPROBANTE_CNT.TERCERO, 
DETALLE_COMPROBANTE_CNT.SUCURSAL, 
CASE WHEN DETALLE_COMPROBANTE_CNT.NRO_DOCUMENTO IS NULL THEN COMPROBANTE_CNT.NRO_DOCUMENTO ELSE DETALLE_COMPROBANTE_CNT.NRO_DOCUMENTO END AS NODOC,
CASE WHEN DETALLE_COMPROBANTE_CNT.DESCRIPCION IS NULL THEN COMPROBANTE_CNT.DESCRIPCION ELSE DETALLE_COMPROBANTE_CNT.DESCRIPCION END AS DESCRIP,
DETALLE_COMPROBANTE_CNT.VALOR_DEBITO, 
DETALLE_COMPROBANTE_CNT.VALOR_CREDITO, 
DETALLE_COMPROBANTE_CNT.FECHA, 
TIPO_COMPROBANTE.CLASE_CONTABLE
FROM (((COMPROBANTE_CNT 
    INNER JOIN DETALLE_COMPROBANTE_CNT ON
    COMPROBANTE_CNT.COMPANIA = DETALLE_COMPROBANTE_CNT.COMPANIA AND 
    COMPROBANTE_CNT.ANO = DETALLE_COMPROBANTE_CNT.ANO AND 
    COMPROBANTE_CNT.TIPO = DETALLE_COMPROBANTE_CNT.TIPO_CPTE AND 
    COMPROBANTE_CNT.NUMERO = DETALLE_COMPROBANTE_CNT.COMPROBANTE)
INNER JOIN TIPO_COMPROBANTE ON 
COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA AND
COMPROBANTE_CNT.TIPO = TIPO_COMPROBANTE.CODIGO)
INNER JOIN V_PLAN_CONTABLE PLAN_CONTABLE ON 
DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA AND
DETALLE_COMPROBANTE_CNT.CUENTA = PLAN_CONTABLE.ID AND 
DETALLE_COMPROBANTE_CNT.ANO = PLAN_CONTABLE.ANO)
INNER JOIN TERCERO ON 
COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA AND
COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL AND 
COMPROBANTE_CNT.TERCERO = TERCERO.NIT
WHERE COMPROBANTE_CNT.COMPANIA=s$compania$s
    AND COMPROBANTE_CNT.ANO=s$anioInicial$s
    AND DETALLE_COMPROBANTE_CNT.CUENTA
        BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
    AND TIPO_COMPROBANTE.CLASE_CONTABLE='E' 
    AND PLAN_CONTABLE.CLASECUENTA='B' 
    AND DETALLE_COMPROBANTE_CNT.PAGADOBANCO IN (0)
    AND DETALLE_COMPROBANTE_CNT.FECHA
        BETWEEN s$fechaInicial$s AND s$fechaFinal$s
