INNER JOIN TERCERO T 
    ON PJ.COMPANIA = T.COMPANIA
   AND PJ.TERCERO = T.NIT
   AND PJ.SUCURSAL = T.SUCURSAL
LEFT JOIN (
    SELECT DISTINCT
        OST.COMPANIA,
        OST.CUENTA,
        OST.NUMEROPROCESO,
        1 AS PAGO
    FROM DETALLE_COMPROBANTE_CNT OST
    INNER JOIN DETALLE_COMPROBANTE_CNT PAGOS
        ON OST.COMPANIA = PAGOS.COMPANIA
       AND OST.ANO = PAGOS.ANO_AFECT 
       AND OST.TIPO_CPTE = PAGOS.TIPO_CPTE_AFECT
       AND OST.COMPROBANTE = PAGOS.CMPTE_AFECTADO
    WHERE OST.TIPO_CPTE = 'OST'
      AND OST.CMPTE_AFECTADO IS NOT NULL
      AND OST.NUMEROPROCESO IS NOT NULL
      AND PAGOS.TIPO_CPTE = 'EG1'
) PAGOS_HAY
   ON PJ.COMPANIA = PAGOS_HAY.COMPANIA
  AND PJ.CODIGO = PAGOS_HAY.CUENTA
  AND PJ.NUMEROPROCESO = PAGOS_HAY.NUMEROPROCESO
WHERE PJ.COMPANIA = s$compania$s
s$condicionValorActual$s
