    PJ.CODIGO AS "CODIGO CONTABLE", 
    T.NOMBRE,
    PJ.TERCERO AS IDENTIFICACION, 
    PJ.NUMEROPROCESO AS "NUMERO DE PROCESO",
    PJ.VALOR AS "VALOR INICIAL",
    TO_CHAR(PJ.FECHA_INGRESO,'DD/MM/YYYY') AS "FECHA INGRESO", 
    PJ.COMPROBANTE_INGRESO AS "COMPROBANTE INGRESO",
    PJ.MODIFICACIONES, 
    PJ.VALOR_ACTUAL AS "VALOR ACTUAL",  
    NVL(PAGOS_HAY.PAGO, 0) AS PAGO
