SELECT C.COMPANIA, C.TIPO TIPO_CPTE, 
	C.NUMERO COMPROBANTE, 
	C.FECHA FECHA_CPTE, 
	D.FECHA FECHA_DETCPTE, 
	D.VALOR_DEBITO, 
	D.VALOR_CREDITO,
  D.CUENTA
FROM COMPROBANTE_CNT C
	INNER JOIN DETALLE_COMPROBANTE_CNT D
  		ON C.COMPANIA = D.COMPANIA
 		AND C.ANO 	  = D.ANO  
 		AND C.TIPO 	  = D.TIPO_CPTE
 		AND C.NUMERO  = D.COMPROBANTE
   		AND TRUNC(C.FECHA)   <> TRUNC( D.FECHA)
WHERE C.COMPANIA  = s$compania$s
  	AND C.FECHA BETWEEN TO_DATE('s$fechaInicial$s') AND TO_DATE('s$fechaFinal$s')
  	AND D.CUENTA  ='s$cuentaCaja$s'
