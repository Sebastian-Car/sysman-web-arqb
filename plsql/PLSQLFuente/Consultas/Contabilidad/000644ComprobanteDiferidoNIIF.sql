SELECT  
D.ANO, 
D.TIPO_CPTE AS TIPO,  
D.COMPROBANTE AS NUMERO,  
D.TERCERO,  
(D.VALOR_DEBITO -(D.DEBITOSAFECTADOS_CXP + D.CREDITOSAFECTADOS_CXP) - COM.ABONADO) AS SALDO,  
COM.FECHA_VCN_DOC 
FROM PLAN_CONTABLE PC  
INNER JOIN DETALLE_COMPROBANTE_CNT D 
ON PC.ANO = D.ANO 
AND PC.COMPANIA = D.COMPANIA 
AND PC.CODIGO = D.CUENTA 
INNER JOIN TIPO_COMPROBANTE T  
ON D.TIPO_CPTE = T.CODIGO  
AND D.COMPANIA = T.COMPANIA  
INNER JOIN COMPROBANTE_CNT COM 
ON D.COMPROBANTE = COM.NUMERO  
AND D.TIPO_CPTE = COM.TIPO 
AND D.ANO = COM.ANO 
AND D.COMPANIA = COM.COMPANIA  
WHERE D.COMPANIA= s$companiaSeleccionada$s 
AND T.CLASE_CONTABLE IN ('V')  
AND D.TERCERO BETWEEN 's$terceroInicial$s' AND 's$terceroFinal$s'  
AND (D.VALOR_DEBITO - (D.DEBITOSAFECTADOS_CXP + D.CREDITOSAFECTADOS_CXP) - COM.ABONADO) > 0 
AND COM.FECHA_VCN_DOC <= TO_DATE('s$fechacorte$s','DD/MM/YYYY') 
AND MONTHS_BETWEEN(TO_DATE('s$fechacorte$s','DD/MM/YYYY'),COM.FECHA_VCN_DOC) >= s$mesesVencidos$s 
AND (CASE WHEN D.FECHA_CALC_DET IS NOT NULL THEN MONTHS_BETWEEN (TO_DATE('s$fechacorte$s','DD/MM/YYYY'),D.FECHA_CALC_DET) ELSE MONTHS_BETWEEN(TO_DATE('s$fechacorte$s','DD/MM/YYYY'), COM.FECHA_VCN_DOC) END) > 0 
AND PC.APLICA_DETERIORO NOT IN (0) 
ORDER BY  
D.ANO,  
D.TIPO_CPTE,  
D.COMPROBANTE, 
COM.FECHA_VCN_DOC
