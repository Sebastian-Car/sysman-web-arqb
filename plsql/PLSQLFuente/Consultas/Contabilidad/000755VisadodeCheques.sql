SELECT
	TIPO_CPTE,
	COMPROBANTE,
	FECHA,
	CODIGO_CUENTA,
	CUENTANOMBRE,
	TERCERO_COM TERCERO,
	NRO_DOCUMENTO_DET,
  (
	SELECT
		MAX(TERCEROPAGOS.CUENTA)
	FROM
		TERCEROPAGOS
	WHERE 
		V_DETALLE_AUXILIAR_CNT.COMPANIA = TERCEROPAGOS.COMPANIA
		AND V_DETALLE_AUXILIAR_CNT.TERCERO_COM = TERCEROPAGOS.NIT
		AND V_DETALLE_AUXILIAR_CNT.SUCURSAL_COM = TERCEROPAGOS.SUCURSAL
		AND
		TERCEROPAGOS.ESTADO_CUENTA = 'A'
		AND TERCEROPAGOS.ACTIVA= -1) AS CTA,
	TERCERO_COM_NOM NOMBRE,
	DESCRIPCION_DET DESCRIPCION,
	VALOR_CREDITO
FROM V_DETALLE_AUXILIAR_CNT 
WHERE COMPANIA  = s$compania$s 
AND CLASECUENTA ='B'
AND CODIGO_CUENTA= 's$cuentaBancaria$s'  
AND NRO_DOCUMENTO_DET= 's$cheque$s'  
AND VALOR_CREDITO>0
AND CLASE_CONTABLE='E'