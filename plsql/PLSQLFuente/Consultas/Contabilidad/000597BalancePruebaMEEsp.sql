SELECT  SUBSTR(SALDOAUX.CODIGO,1,1) CLASE, 
        SALDOAUX.CODIGO, 
        SALDOAUX.DEBITO, 
        SALDOAUX.CREDITO, 
        SALDOAUX.NOMBRE,
        SALDOAUX.COMPANIA, 
        SALDOAUX.TERCERO,
        SALDOANT,        
        SALDOFIN, 
        SUBSTR((CASE WHEN SUBSTR(SALDOAUX.CODIGO,1,1)='0'
             THEN 'z'||SALDOAUX.CODIGO
             ELSE SALDOAUX.CODIGO
             END),1,1) AS CLASEORDEN,
        SALDOAUX.ORDEN ORDENCUENTAS,
           CASE WHEN SUBSTR(SALDOAUX.CODIGO,1,1)='0'
             THEN 'z'||SALDOAUX.CODIGO
             ELSE SALDOAUX.CODIGO
             END AS ORDEN,
           SALDOAUX.CENTRO_COSTO,
           SALDOAUX.REFERENCIA, 
           SALDOAUX.AUXILIAR,
           SALDOAUX.FUENTE_RECURSOS    
FROM (s$baseBalance$s) SALDOAUX 
 WHERE LENGTH(SALDOAUX.CODIGO) <= s$digitos$s
   AND (SALDOAUX.DEBITO         + SALDOAUX.CREDITO 
      + ABS(SALDOAUX.SALDOANT)  + ABS(SALDOAUX.SALDOFIN) 
      ) <>
      CASE WHEN s$saldoCero$s = 0 
      THEN 0
      ELSE SALDOAUX.DEBITO         + SALDOAUX.CREDITO 
         + ABS(SALDOAUX.SALDOANT)  + ABS(SALDOAUX.SALDOFIN)
         + 1 
      END
