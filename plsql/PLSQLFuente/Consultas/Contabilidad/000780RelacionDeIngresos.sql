SELECT DETALLE_COMPROBANTE_CNT.CUENTA AS CODIGO_CUENTA, 
SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO) AS SUMADEVALOR_DEBITO,
SUM(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO) AS SUMADEVALOR_CREDITO, 
MAX(PLAN_CONTABLE.NOMBRE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS ULTIMODENOMBRE, 
MAX(PLAN_PRESUPUESTAL.NOMBRE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS ULTIMODENOMBRE1, 
MAX(PLAN_PRESUPUESTAL.CODIGO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS ULTIMODECODIGO, 
DETALLE_COMPROBANTE_CNT.COMPANIA
FROM ((DETALLE_COMPROBANTE_CNT 
    LEFT JOIN PLAN_CONTABLE ON 
    DETALLE_COMPROBANTE_CNT.CUENTA = PLAN_CONTABLE.CODIGO AND
    DETALLE_COMPROBANTE_CNT.ANO = PLAN_CONTABLE.ANO AND 
    DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA) 
LEFT JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL ON
 PLAN_CONTABLE.CUENTA_PPTAL = PLAN_PRESUPUESTAL.ID AND 
 PLAN_CONTABLE.ANO = PLAN_PRESUPUESTAL.ANO AND
 PLAN_CONTABLE.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA) 
LEFT JOIN TIPO_COMPROBANTE ON 
DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO AND 
DETALLE_COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA=s$compania$s
    AND TO_CHAR(DETALLE_COMPROBANTE_CNT.FECHA,'DD/MM/YYYY')=TO_CHAR(s$fecha$s
,'DD/MM/YYYY')
    AND SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA,0,2)>='12'
    AND TIPO_COMPROBANTE.CLASE_CONTABLE IN ('I','S')
GROUP BY DETALLE_COMPROBANTE_CNT.CUENTA, 
DETALLE_COMPROBANTE_CNT.COMPANIA, 
TIPO_COMPROBANTE.CLASE_CONTABLE
