MERGE INTO CONSULTAS FIN USING (SELECT '000697BalanceGeneralPorTercero' INFORME ,TO_CLOB(q'[SELECT COMPANIA, 
     CODIGO, 
     TERCERO,
     TERCERO_NOMBRE, 
      NOMBRE,   
      DEBITOMES,
      CREDITOMES,
      CLASEORDEN,  
CASE WHEN (NATURALEZA = 'D' AND SALDOANT>= 0 OR NATURALEZA = 'C' AND SALDOANT<=0) 
     THEN SALDOANT
     ELSE 0
     END  SALDOANTDEBITO,          
      CASE WHEN (NATURALEZA = 'D' AND SALDOANT<= 0 OR NATURALEZA = 'C' AND SALDOANT>=0)
     THEN SALDOANT
     ELSE 0
     END 
     SALDOANTCREDITO,
     CASE WHEN (NATURALEZA = 'D' AND SALDOFIN >= 0 OR NATURALEZA = 'C' AND SALDOFIN<=0)
     THEN SALDOFIN
     ELSE 0
     END 
     SALDONUEDEBITO,
     CASE WHEN (NATURALEZA = 'D' AND SALDOFIN <= 0 OR NATURALEZA = 'C' AND SALDOFIN>=0 )
     THEN SALDOFIN
     ELSE 0
     END
     SALDONUECREDITO
FROM(
SELECT 
     SALDO_AUX_CONTABLE.COMPANIA, 
     SALDO_AUX_CONTABLE.CODIGO, 
     SALDO_AUX_CONTABLE.TERCERO,
     TERCERO.NOMBRE TERCERO_NOMBRE, 
     PLAN_CONTABLE.NOMBRE, 
     SALDO_AUX_CONTABLE.NATURALEZA,                        
     SUM(s$mesDebito$s)    DEBITOMES , 
     SUM(s$mesCredito$s)   CREDITOMES, 
     SUBSTR(SALDO_AUX_CONTABLE.CODIGO,1,1)  CLASEORDEN, 
      SUM(SALDO_AUX_CONTABLE.SALDOs$mesAnterior$s) SALDOANT,              
      SUM( SALDO_AUX_CONTABLE.SALDOs$mesActual$s
)  SALDOFIN
   
FROM SALDO_AUX_CONTABLE
     INNER JOIN PLAN_CONTABLE
        ON SALDO_AUX_CONTABLE.COMPANIA = PLAN_CONTABLE.COMPANIA
       AND SALDO_AUX_CONTABLE.ANO      = PLAN_CONTABLE.ANO     
       AND SALDO_AUX_CONTABLE.CODIGO   = PLAN_CONTABLE.CODIGO
     INNER JOIN TERCERO 
          ON SALDO_AUX_CONTABLE.COMPANIA = TERCERO.COMPANIA 
         AND SALDO_AUX_CONTABLE.TERCERO  = TERCERO.NIT 
         AND SALDO_AUX_CONTABLE.SUCURSAL = TERCERO.SUCURSAL          
WHERE SALDO_AUX_CONTABLE.COMPANIA  =  s$compania$s   
  AND SALDO_AUX_CONTABLE.ANO       =  s$anio$s 
  AND SALDO_AUX_CONTABLE.CODIGO  BETWEEN 's$codigoInicial$s'  AND 's$codigoFinal$s'  
  s$condicionAdicional$s
GROUP BY SALDO_AUX_CONTABLE.COMPANIA, 
     SALDO_AUX_CONTABLE.CODIGO, 
     SALDO_AUX_CONTABLE.TERCERO, 
     PLAN_CONTABLE.NOMBRE,    
     SALDO_AUX_CONTABLE.NATURALEZA,
     SUBSTR(SALDO_AUX_CONTABLE.CODIGO,1,1), 
     SUBSTR((CASE WHEN SUBSTR(SALDO_AUX_CONTABLE.CODIGO,1,1) = '0' THEN 'Z'||SALDO_AUX_CONTABLE.CODIGO ELSE SALDO_AUX_CONTABLE.CODIGO END),1,1),
     TERCERO.NOMBRE)
WHERE ABS(SALDOANT)+ ABS(DEBITOMES) + (CREDITOMES) + ABS(SALDOFIN) >0]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, 'dcastiblanco' MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);