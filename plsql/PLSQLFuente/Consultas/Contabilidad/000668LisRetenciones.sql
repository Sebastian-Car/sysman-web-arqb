MERGE INTO CONSULTAS FIN USING (SELECT '000668LisRetenciones' INFORME ,TO_CLOB(q'[SELECT RETENCIONES.ANO,
	RETENCIONES.CUENTA_CREDITO,
	RETENCIONES.CUENTA_DEBITO,
	RETENCIONES.EXPLICACION,
	RETENCIONES.FACTORREDONDEO,
	RETENCIONES.LIMITE_INF,
	RETENCIONES.NOMBRE,
	RETENCIONES.PCT_BASE,
	CASE WHEN RETENCIONES.PERMITEMODIFICAR <> 0 THEN 'x' ELSE ' ' END PERMITEMODIFICAR,
	CASE WHEN RETENCIONES.PERMITEMODIFICARBASE <> 0 THEN 'x' ELSE ' ' END PERMITEMODIFICARBASE,
	RETENCIONES.TIPO,
	RETENCIONES.VALOR_APLICAR, 
  RETENCIONES.PCT_APLICAR,
  RETENCIONES.CODIGO,
	TIPORETENCION.NOMBRE NOMBRERETENCION
FROM RETENCIONES 
LEFT JOIN TIPORETENCION 
	ON RETENCIONES.TIPO = TIPORETENCION.CODIGO
WHERE RETENCIONES.COMPANIA=s$compania$s
	AND RETENCIONES.TIPO BETWEEN 's$tipoInicial$s' AND 's$tipoFinal$s'
	AND RETENCIONES.ANO=s$anio$s
ORDER BY RETENCIONES.COMPANIA, RETENCIONES.TIPO, RETENCIONES.ANO, RETENCIONES.CODIGO]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);