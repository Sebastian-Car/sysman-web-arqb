MERGE INTO CONSULTAS FIN USING (SELECT '002867RelacionIngresosEspecial' INFORME ,TO_CLOB(q'[SELECT
  DETALLE_COMPROBANTE_CNT.FECHA,
  DETALLE_COMPROBANTE_CNT.TIPO_CPTE,
  TO_CHAR(FECHA,'MM') MES,
  DETALLE_COMPROBANTE_CNT.COMPROBANTE,
  CASE
    WHEN s$indicador$s <>0
    AND TIPO_COMPROBANTE.CLASE_CONTABLE IN ('S')
    THEN 'B'
    ELSE TIPO_COMPROBANTE.CLASE_CONTABLE
  END EXPR1,
  TIPO_COMPROBANTE.CLASE_CONTABLE,
  TERCERO.NOMBRE NOMBRETER ,
  DETALLE_COMPROBANTE_CNT.VALOR_DEBITO,
  PLAN_CONTABLE.CLASECUENTA,
  DETALLE_COMPROBANTE_CNT.FECHA,
  DETALLE_COMPROBANTE_CNT.COMPANIA,
  DETALLE_COMPROBANTE_CNT.DESCRIPCION,
  TO_CHAR(FECHA,'YYYY') AS ANO,
  TIPO_COMPROBANTE.CODIGO,
  TIPO_COMPROBANTE.NOMBRE
FROM DETALLE_COMPROBANTE_CNT
LEFT JOIN TIPO_COMPROBANTE
    ON DETALLE_COMPROBANTE_CNT.COMPANIA   = TIPO_COMPROBANTE.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
LEFT JOIN TERCERO
    ON DETALLE_COMPROBANTE_CNT.COMPANIA  = TERCERO.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.TERCERO  = TERCERO.NIT
    AND DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL
LEFT JOIN PLAN_CONTABLE
    ON DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.ANO     = PLAN_CONTABLE.ANO
AND DETALLE_COMPROBANTE_CNT.CUENTA  = PLAN_CONTABLE.CODIGO
WHERE
CASE WHEN s$indicador$s
      <>0 AND TIPO_COMPROBANTE.CLASE_CONTABLE IN ('S')
     THEN 'B'
     ELSE CLASE_CONTABLE
    END IN ('I','B')
AND DETALLE_COMPROBANTE_CNT.VALOR_DEBITO >0
AND PLAN_CONTABLE.CLASECUENTA IN ('B','J')
AND DETALLE_COMPROBANTE_CNT.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
AND DETALLE_COMPROBANTE_CNT.COMPANIA = s$compania$s]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);