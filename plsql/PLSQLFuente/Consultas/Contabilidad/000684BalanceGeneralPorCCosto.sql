SELECT 
       CENTRO_COSTO.NOMBRE CNOMBRE, 
       SALDO.CENTRO_COSTO CENTROCOSTO,    
       SUBSTR(PLAN.CODIGO,1,1) CLASE, 
       SUBSTR(CASE WHEN SUBSTR(PLAN.CODIGO,0,1) = '0' 
       	            THEN 'Z'||PLAN.CODIGO 
       	            ELSE PLAN.CODIGO 
       	       END,1,1) CLASEORDEN, 
       PLAN.CODIGO,
       PLAN.COMPANIA,
       SUM(SALDO.DEBITO) DEBITO,       
       SUM(SALDO.CREDITO) CREDITO, 
       PLAN.NOMBRE NOMBRECUENTA, 
       SUM(SALDO.NUEVOSALDO) NUEVOSALDO,
       CASE WHEN SUBSTR(PLAN.CODIGO,0,1) = '0'
            THEN 'Z'||PLAN.CODIGO 
            ELSE PLAN.CODIGO 
       END ORDEN,
       (PLAN.CODIGO||SALDO.CENTRO_COSTO) ORDENCUENTAS,
       SUM(SALDO.SALDOANTERIOR) SALDOANTERIOR
FROM (
SELECT PLAN_CONTABLE.COMPANIA,  
       PLAN_CONTABLE.ANO,  
       PLAN_CONTABLE.CODIGO,   
       SALDO_AUX_CONTABLE.CENTRO_COSTO,
       SUM(SALDO_AUX_CONTABLE.SALDOs$mesAux$s) SALDOANTERIOR,
       SUM(SALDO_AUX_CONTABLE.SALDOs$mes$s)  NUEVOSALDO,  
       SUM(CASE WHEN (PLAN_CONTABLE.NATURALEZA='C' AND SALDO_AUX_CONTABLE.SALDOs$mesAux$s>0) OR 
                     (PLAN_CONTABLE.NATURALEZA='D' AND SALDO_AUX_CONTABLE.SALDOs$mesAux$s<0)  
                THEN ABS(SALDO_AUX_CONTABLE.SALDOs$mesAux$s) 
                ELSE 0 
           END)  CREDITOANT,         
       SUM(SALDO_AUX_CONTABLE.DEBITOs$mes$s)   DEBITO,  
       SUM(SALDO_AUX_CONTABLE.CREDITOs$mes$s)  CREDITO,
       SUM(CASE WHEN (PLAN_CONTABLE.NATURALEZA='D' AND SALDO_AUX_CONTABLE.SALDOs$mes$s>0) OR 
                     (PLAN_CONTABLE.NATURALEZA='C' AND SALDO_AUX_CONTABLE.SALDOs$mes$s<0)  
                THEN ABS(SALDO_AUX_CONTABLE.SALDO1) 
                ELSE 0 
           END)  DEBITOFINAL,  
       SUM(CASE WHEN (PLAN_CONTABLE.NATURALEZA='C' AND SALDO_AUX_CONTABLE.SALDOs$mes$s>0) OR 
                     (PLAN_CONTABLE.NATURALEZA='D' AND SALDO_AUX_CONTABLE.SALDOs$mes$s<0) 
                THEN ABS(SALDO_AUX_CONTABLE.SALDOs$mes$s) 
                ELSE 0 
           END)  CREDITOFINAL  
FROM PLAN_CONTABLE INNER JOIN SALDO_AUX_CONTABLE 
    ON SALDO_AUX_CONTABLE.COMPANIA = PLAN_CONTABLE.COMPANIA 
   AND SALDO_AUX_CONTABLE.ANO      = PLAN_CONTABLE.ANO 
   AND SALDO_AUX_CONTABLE.CODIGO   = PLAN_CONTABLE.CODIGO    
WHERE PLAN_CONTABLE.COMPANIA       = s$compania$s 
  AND PLAN_CONTABLE.ANO                        =  s$anio$s   
  AND PLAN_CONTABLE.MAN_CEN_CTO        NOT IN(0) 
  AND PLAN_CONTABLE.CODIGO            BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 	
  s$condicionCentroCosto$s		 
  s$condicionReportes$s	 
  s$condicionSaldoCero$s	
GROUP BY PLAN_CONTABLE.COMPANIA,  
         PLAN_CONTABLE.ANO,  
         PLAN_CONTABLE.CODIGO,      
         SALDO_AUX_CONTABLE.CENTRO_COSTO
) SALDO 
    INNER JOIN PLAN_CONTABLE PLAN
	   ON SALDO.COMPANIA                                = PLAN.COMPANIA
	   AND SALDO.ANO                                    = PLAN.ANO
	   AND SUBSTR(SALDO.CODIGO, 1, LENGTH(PLAN.CODIGO)) = PLAN.CODIGO 
	LEFT JOIN 	CENTRO_COSTO 
   		ON  SALDO.COMPANIA     = CENTRO_COSTO.COMPANIA 
   		AND SALDO.CENTRO_COSTO = CENTRO_COSTO.CODIGO 
   		AND SALDO.ANO          = CENTRO_COSTO.ANO 
GROUP BY PLAN.CODIGO, 
         PLAN.COMPANIA, 
         PLAN.ANO,
         SALDO.CENTRO_COSTO,
         PLAN.NOMBRE,
         CENTRO_COSTO.NOMBRE
