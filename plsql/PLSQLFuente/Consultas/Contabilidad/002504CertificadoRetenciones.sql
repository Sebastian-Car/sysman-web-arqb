AND DETALLE_COMPROBANTE_CNT.ANO = BASE_RETENCIONES.ANO
AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = BASE_RETENCIONES.TIPO_CPTE
AND DETALLE_COMPROBANTE_CNT.COMPROBANTE = BASE_RETENCIONES.COMPROBANTE
AND DETALLE_COMPROBANTE_CNT.CONSECUTIVO = BASE_RETENCIONES.CONSECUTIVO
