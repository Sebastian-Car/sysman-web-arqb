WITH  
    BASE AS (SELECT RESUMEN_RENTAS.COMPANIA, 
        RESUMEN_RENTAS.ANO, 
        RESUMEN_RENTAS.TIPO, 
        RESUMEN_RENTAS.NUMERO, 
        COMPROBANTE_CNT.VLR_DOCUMENTO TOTALINGRESO,
        ROUND(RESUMEN_RENTAS.DEDUCCION)             AS DEDUCCION, 
        ROUND(RESUMEN_RENTAS.PAGOS_SALUD)           AS PAGOS_SALUD, 
        ROUND(RESUMEN_RENTAS.APORTES_PENSION)       AS APORTES_PENSION, 
        ROUND(RESUMEN_RENTAS.DEDUCCION_DEPENDIENTE) AS DEDUCCION_DEPENDIENTE, 
        RESUMEN_RENTAS.ARL, 
        ROUND(RESUMEN_RENTAS.APORTE_VOLUNTARIO)     AS APORTE_VOLUNTARIO, 
        ROUND(RESUMEN_RENTAS.DEDUCCION_PREPAGADA)   AS DEDUCCION_PREPAGADA, 
        ROUND(RESUMEN_RENTAS.EXENCION)              AS EXENCION, 
        RESUMEN_RENTAS.VALORUVT, 
        RESUMEN_RENTAS.VALORRETENIDO,  
        TERCERO.NOMBRE,  
        RESUMEN_RENTAS.TERCERO,  
        RESUMEN_RENTAS.FECHA  
      FROM RESUMEN_RENTAS INNER JOIN TERCERO 
          ON RESUMEN_RENTAS.COMPANIA = TERCERO.COMPANIA  
          AND RESUMEN_RENTAS.TERCERO  = TERCERO.NIT 
          AND RESUMEN_RENTAS.SUCURSAL  = TERCERO.SUCURSAL 
          INNER JOIN COMPROBANTE_CNT
                  ON RESUMEN_RENTAS.COMPANIA = COMPROBANTE_CNT.COMPANIA
                  AND RESUMEN_RENTAS.ANO     = COMPROBANTE_CNT.ANO
                  AND RESUMEN_RENTAS.TIPO    = COMPROBANTE_CNT.TIPO
                  AND RESUMEN_RENTAS.NUMERO  = COMPROBANTE_CNT.NUMERO
      WHERE RESUMEN_RENTAS.COMPANIA = s$compania$s 
          AND RESUMEN_RENTAS.ANO    = s$ano$s 
          AND RESUMEN_RENTAS.TIPO   = 's$tipoCpte$s' 
          AND RESUMEN_RENTAS.NUMERO = s$numeroCpte$s 
    ),  
    SUBTOTALES AS (SELECT COMPANIA,ANO, 
        TIPO, 
        NUMERO, 
        TOTALINGRESO, 
        DEDUCCION, 
        PAGOS_SALUD, 
        APORTES_PENSION, 
        DEDUCCION_DEPENDIENTE, 
        ARL, 
        APORTE_VOLUNTARIO, 
        DEDUCCION_PREPAGADA, 
        EXENCION, 
        VALORUVT, 
        VALORRETENIDO,  
        NOMBRE,  
        TERCERO,  
        FECHA,  
        TOTALINGRESO - DEDUCCION - PAGOS_SALUD - APORTES_PENSION - DEDUCCION_DEPENDIENTE - ARL - APORTE_VOLUNTARIO - DEDUCCION_PREPAGADA AS SUBTOTAL, 
        TOTALINGRESO - PAGOS_SALUD - APORTES_PENSION AS SUBTOTAL384 
        FROM BASE 
    ), 
    INGRESOS AS (SELECT  COMPANIA,ANO, 
        TIPO, 
        NUMERO, 
        TOTALINGRESO, 
        DEDUCCION, 
        PAGOS_SALUD, 
        APORTES_PENSION, 
        DEDUCCION_DEPENDIENTE, 
        ARL, 
        APORTE_VOLUNTARIO, 
        DEDUCCION_PREPAGADA, 
        EXENCION, 
        VALORUVT, 
        VALORRETENIDO,  
        NOMBRE,  
        TERCERO,  
        FECHA,  
        SUBTOTAL, 
        SUBTOTAL384, 
        SUBTOTAL-EXENCION AS INGRESO_GRAVABLE,  
        ROUND(SUBTOTAL384/VALORUVT,2) AS INGRESO_UVT2 
        FROM SUBTOTALES) 
SELECT COMPANIA, ANO, 
    TIPO, 
    NUMERO, 
    TOTALINGRESO, 
    DEDUCCION, 
    PAGOS_SALUD, 
    APORTES_PENSION, 
    DEDUCCION_DEPENDIENTE, 
    ARL, 
    APORTE_VOLUNTARIO, 
    DEDUCCION_PREPAGADA, 
    EXENCION, 
    VALORUVT, 
    VALORRETENIDO,  
    TRIM(NOMBRE) NOMBRE,  
    TERCERO,  
    FECHA, 
    TRIM(TO_CHAR(FECHA, 'Month')) NOMBREMES, 
    TO_CHAR(FECHA, 'YYYY') ANOFECHA, 
    SUBTOTAL, 
    SUBTOTAL384, 
    INGRESO_GRAVABLE,  
    INGRESO_UVT2, 
    ROUND(INGRESO_GRAVABLE/VALORUVT,2) AS INGRESO_UVT,  
    ROUND(VALORRETENIDO/VALORUVT,2) AS RETENCION_UVT, 
    ROUND(VALORUVT*PCK_CONTABILIDAD2.FC_RETEF2013_384(COMPANIA,0,INGRESO_UVT2,ANO),-3) AS VALORETENIDO384 
FROM INGRESOS
