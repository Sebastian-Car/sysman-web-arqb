MERGE INTO CONSULTAS FIN USING (SELECT '002391SubFlujosEfeActiOpe' INFORME ,TO_CLOB(q'[SELECT SUM (TOTAL_FINAL) TOTAL_NUEVO,
       SUM (TOTAL_INICIAL)TOTAL_ANTERIOR FROM(SELECT  
    SUM ( CASE WHEN PLAN_CONTABLE.CODIGO='4' THEN PLAN_CONTABLE.SALDOs$mesFinal$s ELSE 0 END)-
    SUM ( CASE WHEN PLAN_CONTABLE.CODIGO='5' THEN PLAN_CONTABLE.SALDOs$mesFinal$s ELSE 0 END) - 
    SUM ( CASE WHEN PLAN_CONTABLE.CODIGO='6' THEN PLAN_CONTABLE.SALDOs$mesFinal$s ELSE 0 END) - 
    SUM ( CASE WHEN PLAN_CONTABLE.CODIGO='7' THEN PLAN_CONTABLE.SALDOs$mesFinal$s ELSE 0 END) AS TOTAL_FINAL,
    0 TOTAL_INICIAL
        FROM PLAN_CONTABLE
        WHERE PLAN_CONTABLE.COMPANIA = s$compania$s
              AND ( PLAN_CONTABLE.CODIGO = '4'
                    OR PLAN_CONTABLE.CODIGO = '5'
                       OR PLAN_CONTABLE.CODIGO = '6'
                          OR PLAN_CONTABLE.CODIGO = '7' )
                  AND PLAN_CONTABLE.ANO = s$anioFinal$s
        GROUP BY PLAN_CONTABLE.COMPANIA,
                 PLAN_CONTABLE.ANO
UNION ALL                 
SELECT              
    0 TOTAL_FINAL,
    SUM ( CASE WHEN PLAN_CONTABLE.CODIGO='4' THEN PLAN_CONTABLE.SALDOs$mesInicial$s ELSE 0 END)-
    SUM ( CASE WHEN PLAN_CONTABLE.CODIGO='5' THEN PLAN_CONTABLE.SALDOs$mesInicial$s ELSE 0 END) - 
    SUM ( CASE WHEN PLAN_CONTABLE.CODIGO='6' THEN PLAN_CONTABLE.SALDOs$mesInicial$s ELSE 0 END) - 
    SUM ( CASE WHEN PLAN_CONTABLE.CODIGO='7' THEN PLAN_CONTABLE.SALDOs$mesInicial$s ELSE 0 END) AS TOTAL_INICIAL
        FROM PLAN_CONTABLE
        WHERE PLAN_CONTABLE.COMPANIA = s$compania$s
              AND ( PLAN_CONTABLE.CODIGO = '4'
                    OR PLAN_CONTABLE.CODIGO = '5'
                       OR PLAN_CONTABLE.CODIGO = '6'
                          OR PLAN_CONTABLE.CODIGO = '7' )
                  AND PLAN_CONTABLE.ANO = s$anioInicial$s
        GROUP BY PLAN_CONTABLE.COMPANIA,
                 PLAN_CONTABLE.ANO)
                 ]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
                   
