MERGE INTO CONSULTAS FIN USING (SELECT '002870RelacionEgresosEspecial' INFORME ,TO_CLOB(q'[SELECT COMPROBANTE_CNT.FECHA,
       TO_CHAR(FECHA,'MM') AS MES,
       TIPO_COMPROBANTE.CLASE_CONTABLE,
       TERCERO.NOMBRE,
       TO_CHAR(FECHA,'YYYY') AS ANO,
       COMPROBANTE_CNT.TIPO,
       COMPROBANTE_CNT.NUMERO,
       COMPROBANTE_CNT.DESCRIPCION,
       CASE 
           WHEN TIPO_COMPROBANTE.CLASE_CONTABLE IN ('D','A')
           THEN COMPROBANTE_CNT.VLRAGIRAR * (-1)
           ELSE COMPROBANTE_CNT.VLRAGIRAR
       END VLRAGIRAR,
       COMPROBANTE_CNT.FECHA,
       COMPROBANTE_CNT.REFERENCIA,
       COMPROBANTE_CNT.COMPANIA
FROM  COMPROBANTE_CNT
     INNER JOIN TIPO_COMPROBANTE
            ON COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA
           AND COMPROBANTE_CNT.TIPO     = TIPO_COMPROBANTE.CODIGO
      INNER JOIN TERCERO
             ON COMPROBANTE_CNT.COMPANIA  = TERCERO.COMPANIA
            AND COMPROBANTE_CNT.TERCERO   = TERCERO.NIT
            AND COMPROBANTE_CNT.SUCURSAL  = TERCERO.SUCURSAL
WHERE COMPROBANTE_CNT.COMPANIA = s$compania$s 
      AND COMPROBANTE_CNT.FECHA BETWEEN s$fechaPrimera$s AND s$fechaFinal$s
      AND TIPO_COMPROBANTE.CLASE_CONTABLE IN ('E','D','A','G')
ORDER BY  COMPROBANTE_CNT.FECHA, 
          COMPROBANTE_CNT.NUMERO]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);   
