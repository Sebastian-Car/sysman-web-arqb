SELECT TIPO_COMPROBANTE.NOMBRE  NOMBRETIPO,
  PLAN_CONTABLE.NOMBRE||'/'|| TERCERO.NOMBRE TOTALNOMBRE,
   DETALLE_COMPROBANTE_CNT.CUENTA CUENTAI,
   DETALLE_COMPROBANTE_CNT.FECHA  FECHAI,
   DETALLE_COMPROBANTE_CNT.VALOR_DEBITO DEBITOI,  
   DETALLE_COMPROBANTE_CNT.VALOR_CREDITO CREDITOI,
   DETALLE_COMPROBANTE_CNT.TERCERO TERCEROI,
   DETALLE_COMPROBANTE_CNT.CENTRO_COSTO CENTROI,
   DETALLE_COMPROBANTE_CNT.COMPROBANTE NUMEROI,
   DETALLE_COMPROBANTE_CNT.TIPO_CPTE TIPOI,
   TERCERO.NOMBRE,
   DETALLE_COMPROBANTE_CNT.COMPANIA,
   DETALLE_COMPROBANTE_CNT.ANO
,
  DETALLE_COMPROBANTE_CNT.MES
,
  UPPER(PCK_SYSMAN_UTL.FC_NOMBRE_MES(DETALLE_COMPROBANTE_CNT.MES)) NOMBREMES
FROM DETALLE_COMPROBANTE_CNT 
  LEFT JOIN TIPO_COMPROBANTE
    ON DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
    AND DETALLE_COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA
  LEFT JOIN PLAN_CONTABLE
    ON DETALLE_COMPROBANTE_CNT.COMPANIA       = PLAN_CONTABLE.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.ANO           = PLAN_CONTABLE.ANO
    AND DETALLE_COMPROBANTE_CNT.CUENTA = PLAN_CONTABLE.CODIGO
 LEFT JOIN TERCERO
    ON DETALLE_COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA 
    AND DETALLE_COMPROBANTE_CNT.TERCERO  = TERCERO.NIT
    AND DETALLE_COMPROBANTE_CNT.SUCURSAL  = TERCERO.SUCURSAL
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA= 's$compania$s'  
   AND DETALLE_COMPROBANTE_CNT.ANO = s$ano$s
   AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = 's$tipoCpte$s'
   AND DETALLE_COMPROBANTE_CNT.COMPROBANTE BETWEEN s$numeroPptoInicial$s AND s$numeroPptoFinal$s
ORDER BY DETALLE_COMPROBANTE_CNT.COMPROBANTE
