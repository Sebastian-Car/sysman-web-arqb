SELECT PLAN_CONTABLE.COMPANIA,
  PLAN_CONTABLE.ANO,
  PLAN_CONTABLE.CODIGO CUENTA,
  SUBSTR(PLAN_CONTABLE.CODIGO,0,4) CLASE,
  (MOVIMIENTOS.ENTRADAS) ENTRADAS,
  (MOVIMIENTOS.SALIDAS) SALIDAS,
  PLAN_CONTABLE.NOMBRE NOMBRECUENTA,
  PLAN_CONTABLE.SALDOs$mesAnterior$s + NVL(FRACCION.SALDOALDIA,0) SALDOANTERIOR,
  PLAN_CONTABLE.SALDOs$mesAnterior$s + NVL(FRACCION.SALDOALDIA,0)+NVL(MOVIMIENTOS.ENTRADAS,0)-NVL(MOVIMIENTOS.SALIDAS,0) SALDOFINAL,
  BANCO.BANCO,
  CUENTABANCOS.RECURSOS,
  PLAN_CONTABLE.MOVIMIENTO MOV,
  BANCO.NOMBREBANCO,
  FUENTE_RECURSOS.NOMBRE NOMBRE
FROM PLAN_CONTABLE
LEFT JOIN
  (SELECT PLANC.COMPANIA,
    PLANC.ANO,
    PLANC.CODIGO,
    SUM(
    CASE
      WHEN PLANC.NATURALEZA='D'
      THEN DETFRACC.VALOR_DEBITO  -DETFRACC.VALOR_CREDITO
      ELSE DETFRACC.VALOR_CREDITO -DETFRACC.VALOR_DEBITO
    END) SALDOALDIA
  FROM DETALLE_COMPROBANTE_CNT DETFRACC
  INNER JOIN V_PLAN_CONTABLE PLANC
  ON DETFRACC.COMPANIA = PLANC.COMPANIA
  AND DETFRACC.ANO     = PLANC.ANO
  AND DETFRACC.CUENTA  = PLANC.CODIGO
  WHERE PLANC.COMPANIA = s$compania$s
  AND PLANC.ANO        = s$anio$s
  AND DETFRACC.FECHA  >= TO_DATE('s$fechaInicialMes$s', 'DD/MM/YYYY' )
  AND DETFRACC.FECHA   < TO_DATE('s$fechaInicial$s', 'DD/MM/YYYY' )
  GROUP BY PLANC.COMPANIA,
    PLANC.ANO,
    PLANC.CODIGO
  ) FRACCION ON PLAN_CONTABLE.COMPANIA = FRACCION.COMPANIA
AND PLAN_CONTABLE.ANO                  = FRACCION.ANO
AND PLAN_CONTABLE.CODIGO               = FRACCION.CODIGO
LEFT JOIN
  (SELECT V_PLAN_CONTABLE.COMPANIA,
    V_PLAN_CONTABLE.CODIGO,
    V_PLAN_CONTABLE.ANO,
    SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO) ENTRADAS,
    SUM(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO) SALIDAS
  FROM V_PLAN_CONTABLE
  INNER JOIN DETALLE_COMPROBANTE_CNT
  ON V_PLAN_CONTABLE.COMPANIA    = DETALLE_COMPROBANTE_CNT.COMPANIA
  AND V_PLAN_CONTABLE.ANO        = DETALLE_COMPROBANTE_CNT.ANO
  AND V_PLAN_CONTABLE.CODIGO     = SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA, 1, LENGTH(V_PLAN_CONTABLE.CODIGO))
  WHERE V_PLAN_CONTABLE.COMPANIA = s$compania$s
  AND V_PLAN_CONTABLE.ANO        = s$anio$s
  AND DETALLE_COMPROBANTE_CNT.FECHA BETWEEN TO_DATE('s$fechaInicial$s', 'DD/MM/YYYY' ) AND TO_DATE('s$fechaFinal$s', 'DD/MM/YYYY' )
  GROUP BY V_PLAN_CONTABLE.COMPANIA,
    V_PLAN_CONTABLE.ANO,
    V_PLAN_CONTABLE.CODIGO
  ) MOVIMIENTOS ON PLAN_CONTABLE.COMPANIA = MOVIMIENTOS.COMPANIA
AND PLAN_CONTABLE.ANO                     = MOVIMIENTOS.ANO
AND PLAN_CONTABLE.CODIGO                  = MOVIMIENTOS.CODIGO
LEFT JOIN CUENTABANCOS
ON PLAN_CONTABLE.CODIGO    = CUENTABANCOS.IDCONTABLE
AND PLAN_CONTABLE.ANO      = CUENTABANCOS.ANO
AND PLAN_CONTABLE.COMPANIA = CUENTABANCOS.COMPANIA
LEFT JOIN FUENTE_RECURSOS
ON CUENTABANCOS.COMPANIA  = FUENTE_RECURSOS.COMPANIA
AND CUENTABANCOS.RECURSOS = FUENTE_RECURSOS.CODIGO
AND CUENTABANCOS.ANO      = FUENTE_RECURSOS.ANO
LEFT JOIN BANCO
ON CUENTABANCOS.BANCO        = BANCO.BANCO
AND CUENTABANCOS.COMPANIA    = BANCO.COMPANIA
WHERE PLAN_CONTABLE.COMPANIA = s$compania$s
AND ( (BANCO.BANCO BETWEEN s$bancoInicial$s AND s$bancoFinal$s
AND CUENTABANCOS.RECURSOS BETWEEN s$fuenteInicial$s AND s$fuenteFinal$s)
OR (BANCO.BANCO               IS NULL) )
AND PLAN_CONTABLE.ANO          = s$anio$s
AND PLAN_CONTABLE.CLASECUENTA IN('B','J')
ORDER BY BANCO,  RECURSOS
