MERGE INTO CONSULTAS FIN USING (SELECT '000690BalancePorTercero' INFORME ,TO_CLOB(q'[SELECT COMPANIA,
       CODIGO,
       TERCERO,
       TERCERO_NOMBRE,
       NOMBRE,
       SUBSTR(CODIGO,1,1)   CLASE, 
       ABS(CASE WHEN (NATURALEZA='D' AND SALDOANT >=0 OR NATURALEZA='C' AND SALDOANT <=0)
        THEN SALDOANT 
        ELSE 0
        END) SALDOANTDEBITO,
       ABS(CASE WHEN (NATURALEZA='D' AND SALDOANT <=0 OR NATURALEZA='C' AND SALDOANT >=0)
        THEN SALDOANT 
        ELSE 0
        END) SALDOANTCREDITO,
       DEBITO DEBITOMES,
       CREDITO CREDITOMES,
       CASE WHEN NATURALEZA = 'D' AND SALDOFIN >= 0 
            THEN SALDOFIN 
            ELSE CASE WHEN NATURALEZA = 'C' AND SALDOFIN < 0 
                      THEN SALDOFIN 
                      ELSE 0 
                 END 
       END SALDONUEDEBITO,
       CASE WHEN NATURALEZA = 'C' AND SALDOFIN >= 0 
         THEN SALDOFIN 
         ELSE CASE WHEN NATURALEZA = 'D' AND SALDOFIN < 0
                   THEN SALDOFIN
                   ELSE 0
              END
   END SALDONUECREDITO,
   SUBSTR((CASE WHEN SUBSTR(CODIGO,1,1) = '0' THEN 'Z'||CODIGO ELSE CODIGO END),1,1) CLASEORDEN
FROM(
SELECT COMPANIA,ANO,CODIGO,NATURALEZA,DEBITOA,CREDITOA,
       DEBITOM,CREDITOM,DEBITO,CREDITO,SALDOANT,SALDOFIN,
       TERCERO,AUXILIAR,CENTRO_COSTO,REFERENCIA,FUENTE_RECURSOS,
       TERCERO_NOMBRE,AUXILIAR_NOMBRE,CENTRO_COSTO_NOMBRE,REFERENCIA_NOMBRE,
       FUENTE_RECURSOS_NOMBRE,NOMBRE,ORDEN,
       (CASE WHEN NATURALEZA = 'D' 
            THEN (DEBITOA + DEBITOM)-(CREDITOA + CREDITOM)
            ELSE (CREDITOA + CREDITOM)-(DEBITOA + DEBITOM)
       END) SALDOFINAL
FROM(s$consultaBase$s)) BASE

ORDER BY CODIGO]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);