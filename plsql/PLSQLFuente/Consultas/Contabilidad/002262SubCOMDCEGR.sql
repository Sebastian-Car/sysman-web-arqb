MERGE INTO CONSULTAS FIN USING (SELECT '002262SubCOMDCEGR' INFORME ,TO_CLOB(q'[SELECT DC.COMPANIA  COMPANIA,
  DC.ANO            ANO,
  DC.TIPO_CPTE      TIPO,
  DC.COMPROBANTE    COMPROBANTE,
  DC.CODIGO_CPC,
  V_PLAN_PRESUPUESTAL.NOMBRE PRIMERODENOMBRE,
  DC.CUENTA || ' ' || DC1.FUENTE_RECURSO CUENTA,
  DC2.COMPROBANTE                        NUMERORES,
  DC2.CMPTE_AFECTADO                     NUMERODIS,
  DC.VALOR_DEBITO-DC.VALOR_CREDITO       VALORNETO,
  DC2.TIPO_CPTE                          TIPO_CPTE2,
  DC2.COMPROBANTE                        NUMERORES1,
  DC2.TIPO_CPTE_AFECT                    TIPO_CPTE_AFECT2,
  DC2.CMPTE_AFECTADO                     NUMERODIS1,
  DC1.TIPO_CPTE                          TIPO_CPTE1,
  DC1.COMPROBANTE                        NORES,
  DC1.TIPO_CPTE_AFECT                    TIPO_CPTE_AFECT1,
  DC1.CMPTE_AFECTADO                     NODIS ,
  DC1.FUENTE_RECURSO 		         AUXILIAR

FROM DETALLE_COMPROBANTE_PPTAL DC
  INNER JOIN V_PLAN_PRESUPUESTAL
   ON DC.COMPANIA    = V_PLAN_PRESUPUESTAL.COMPANIA
    AND DC.ANO        = V_PLAN_PRESUPUESTAL.ANO
    AND DC.CUENTA        = V_PLAN_PRESUPUESTAL.CODIGO
    AND DC.CENTRO_COSTOI = V_PLAN_PRESUPUESTAL.CENTRO_COSTO
    AND DC.AUXILIARI      = V_PLAN_PRESUPUESTAL.AUXILIAR
    AND DC.REFERENCIAI  = V_PLAN_PRESUPUESTAL.REFERENCIA
    AND DC.FUENTE_RECURSOI         = V_PLAN_PRESUPUESTAL.FUENTE_RECURSO
  LEFT JOIN DETALLE_COMPROBANTE_PPTAL DC1
    ON DC.COMPANIA         = DC1.COMPANIA
    AND DC.ANO             = DC1.ANO
    AND DC.TIPO_CPTE_AFECT = DC1.TIPO_CPTE
    AND DC.CMPTE_AFECTADO  = DC1.COMPROBANTE
    AND DC.CONSECUTIVOPPTO = DC1.CONSECUTIVO
  LEFT JOIN DETALLE_COMPROBANTE_PPTAL DC2
    ON DC1.COMPANIA         = DC2.COMPANIA
    AND DC1.ANO             = DC2.ANO
    AND DC1.TIPO_CPTE_AFECT = DC2.TIPO_CPTE
    AND DC1.CMPTE_AFECTADO  = DC2.COMPROBANTE
    AND DC1.CONSECUTIVOPPTO = DC2.CONSECUTIVO  
WHERE DC.COMPANIA = '$P!{PR_COMPANIA}'
AND DC.ANO        = $P!{PR_ANO}
AND DC.TIPO_CPTE  = '$P!{PR_TIPO}'
AND DC.COMPROBANTE = '$P!{PR_COMPROBANTE}']') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);

