SELECT  
    COMPANIA,
    TIPO_CPTE,
    CUENTANOMBRE PLAN_CONTABLE_NOMBRE,
    TERCERO_NOM NOMBRETERCERO,
    COMPROBANTE,
    FECHA,
    DESCRIPCION,
    NRO_DOCUMENTO,
    SUM(VALOR_DEBITO) VALOR_DEBITO, 
    TIPO_COMPROBANTE_NOM TIPO_COMPROBANTE_NOMBRE,
    CODIGO_CUENTA,
SUM( VALOR_CREDITO) VALOR_CREDITO, 
    TERCERO CODTERCERO,      
    CASE WHEN VALOR_DEBITO NOT IN (0)
         THEN 0
         ELSE 1
         END CAMPO1,
    CODIGO_CUENTA||CASE WHEN CENTRO_COSTO_DET IS NULL
                        THEN ''
                        ELSE CENTRO_COSTO_NOM
                        END
                 ||CASE WHEN TERCERO_DET IS NULL
                        THEN ''
                        ELSE TERCERO_NOM
                        END 
                 ||CASE WHEN AUXILIAR_DET IS NULL
                        THEN ''
                        ELSE AUXILIAR_NOM
                        END 
                  ||CUENTANOMBRE AS IDORDEN  
FROM  V_DETALLE_AUXILIAR_CNT 
   INNER JOIN COMPANIA  
     ON V_DETALLE_AUXILIAR_CNT.COMPANIA = COMPANIA.CODIGO 
WHERE 
V_DETALLE_AUXILIAR_CNT.COMPANIA = s$compania$s
AND V_DETALLE_AUXILIAR_CNT.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
AND V_DETALLE_AUXILIAR_CNT.TIPO_CPTE BETWEEN s$tipoInicial$s AND s$tipoFinal$s
AND V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s 
s$condCosto$s 
GROUP BY COMPANIA, TIPO_CPTE, CUENTANOMBRE, TERCERO_NOM, COMPROBANTE, 
FECHA, DESCRIPCION, NRO_DOCUMENTO, TIPO_COMPROBANTE_NOM, CODIGO_CUENTA, 
TERCERO,COMPROBANTE,
  FECHA,
  DESCRIPCION,
  NRO_DOCUMENTO,
  TIPO_COMPROBANTE_NOM,
TERCERO,
 CASE WHEN VALOR_DEBITO NOT IN (0)
    THEN 0
    ELSE 1
  END ,
   CODIGO_CUENTA|| CASE
    WHEN CENTRO_COSTO_DET IS NULL
    THEN ''
    ELSE CENTRO_COSTO_NOM
  END
  ||
  CASE
    WHEN TERCERO_DET IS NULL
    THEN ''
    ELSE TERCERO_NOM
  END
  ||
  CASE
    WHEN AUXILIAR_DET IS NULL
    THEN ''
    ELSE AUXILIAR_NOM
  END
  ||CUENTANOMBRE

ORDER BY V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA,
V_DETALLE_AUXILIAR_CNT.FECHA,
V_DETALLE_AUXILIAR_CNT.COMPROBANTE
