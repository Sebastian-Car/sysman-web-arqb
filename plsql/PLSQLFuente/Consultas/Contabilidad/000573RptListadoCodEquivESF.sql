SELECT     PLAN_CONTABLE.COMPANIA, 
           PLAN_CONTABLE.ANO, 
           PLAN_CONTABLE.CODIGO, 
           PLAN_CONTABLE.NOMBRE AS NOMBRECTACONT, 
           NVL(PLAN_PRESUPUESTAL.CODIGO,' ') CUENTA_PPTAL, 
           NVL(PLAN_PRESUPUESTAL.NOMBRE,' ') NOMBRECTAPPTAL
 FROM      PLAN_CONTABLE 
 LEFT JOIN PLAN_PPTAL_CUENTACNT  
        ON PLAN_CONTABLE.COMPANIA     = PLAN_PPTAL_CUENTACNT.COMPANIA
       AND PLAN_CONTABLE.ANO          = PLAN_PPTAL_CUENTACNT.ANO
	     AND PLAN_CONTABLE.CODIGO       = PLAN_PPTAL_CUENTACNT.CUENTA_CONTABLE  
 LEFT JOIN PLAN_PRESUPUESTAL 
        ON PLAN_PPTAL_CUENTACNT.COMPANIA     = PLAN_PRESUPUESTAL.COMPANIA
       AND PLAN_PPTAL_CUENTACNT.ANO          = PLAN_PRESUPUESTAL.ANO
	     AND PLAN_PPTAL_CUENTACNT.RUBRO        = PLAN_PRESUPUESTAL.CODIGO       
 WHERE     PLAN_CONTABLE.COMPANIA = s$compania$s
  AND      PLAN_CONTABLE.ANO = s$anio$s
  AND      PLAN_CONTABLE.CODIGO  BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 
