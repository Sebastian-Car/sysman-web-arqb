WITH  TMPBALANCEDEPENDENCIA AS(
  SELECT      PLAN_CONTABLE.CODIGO, 
              DETALLE_COMPROBANTE_CNT.VALOR_DEBITO     DEBITO, 
              DETALLE_COMPROBANTE_CNT.VALOR_CREDITO    CREDITO, 
              DEPENDENCIA.NOMBRE AS DEPENDENCIA, 
              TERCERO.NOMBRE AS RESPONSABLE  
   FROM       PLAN_CONTABLE 
   INNER JOIN DETALLE_COMPROBANTE_CNT ON (
              PLAN_CONTABLE.CODIGO     = DETALLE_COMPROBANTE_CNT.CUENTA
          AND PLAN_CONTABLE.ANO        = DETALLE_COMPROBANTE_CNT.ANO
          AND PLAN_CONTABLE.COMPANIA   = DETALLE_COMPROBANTE_CNT.COMPANIA)
   INNER JOIN DEPENDENCIA_RESPONSABLE ON (
              DETALLE_COMPROBANTE_CNT.COMPANIA = DEPENDENCIA_RESPONSABLE.COMPANIA
          AND DETALLE_COMPROBANTE_CNT.D_DEPENDENCIACNT = DEPENDENCIA_RESPONSABLE.DEPENDENCIA
          AND DETALLE_COMPROBANTE_CNT.D_RESPONSABLECNT = DEPENDENCIA_RESPONSABLE.RESPONSABLE
          AND DETALLE_COMPROBANTE_CNT.D_RESPSUCURSALCNT  = DEPENDENCIA_RESPONSABLE.SUCURSAL)
   INNER JOIN DEPENDENCIA ON (
              DEPENDENCIA_RESPONSABLE.COMPANIA  = DEPENDENCIA.COMPANIA
          AND DEPENDENCIA_RESPONSABLE.DEPENDENCIA = DEPENDENCIA.CODIGO)
   INNER JOIN RESPONSABLE ON (
              DEPENDENCIA_RESPONSABLE.COMPANIA = RESPONSABLE.COMPANIA
          AND DEPENDENCIA_RESPONSABLE.RESPONSABLE = RESPONSABLE.CEDULA
          AND DEPENDENCIA_RESPONSABLE.SUCURSAL = RESPONSABLE.SUCURSAL)
   INNER JOIN TERCERO   ON (
              RESPONSABLE.COMPANIA = TERCERO.COMPANIA
          AND RESPONSABLE.CEDULA = TERCERO.NIT
          AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL)
   WHERE      PLAN_CONTABLE.COMPANIA   = s$compania$s
   AND        PLAN_CONTABLE.ANO        = s$anio$s
   AND        PLAN_CONTABLE.MOVIMIENTO NOT IN (0)
   AND        PLAN_CONTABLE.CODIGO     BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
   AND        LENGTH(PLAN_CONTABLE.CODIGO)  <= s$numeroDigitos$s
   AND        TO_NUMBER(TO_CHAR(DETALLE_COMPROBANTE_CNT.FECHA,'MM')) = s$mes$s 
   ORDER BY   PLAN_CONTABLE.CODIGO
   )
  SELECT     CODIGO, 
             SUM(TMPBALANCEDEPENDENCIA.DEBITO)        DEBITO, 
             SUM(TMPBALANCEDEPENDENCIA.CREDITO)       CREDITO, 
             DEPENDENCIA, 
             RESPONSABLE  
    FROM     TMPBALANCEDEPENDENCIA  
    GROUP BY CODIGO, DEPENDENCIA, RESPONSABLE 
    ORDER BY CODIGO, DEPENDENCIA
