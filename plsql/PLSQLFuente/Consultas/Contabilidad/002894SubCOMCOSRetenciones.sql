MERGE INTO CONSULTAS FIN USING (SELECT '002894SubCOMCOSRetenciones' INFORME ,TO_CLOB(q'[SELECT DISTINCT COMPROBANTE_CNTRETENCION.COMPANIA COMP,
  COMPROBANTE_CNTRETENCION.ANO ANOCOMP,
  COMPROBANTE_CNTRETENCION.TIPO TIPOCOMP,
  COMPROBANTE_CNTRETENCION.NUMERO NUMCOMP,
  RETENCIONES.NOMBRE,
  RETENCIONES.PCT_APLICAR,
  (
  CASE
    WHEN ALEY1450 <> 0
    THEN VALORBASELEY1607
    ELSE COMPROBANTE_CNTRETENCION.VALORBASE
  END) VALORBASE
FROM COMPROBANTE_CNTRETENCION
LEFT JOIN RETENCIONES
ON COMPROBANTE_CNTRETENCION.COMPANIA         = RETENCIONES.COMPANIA
AND COMPROBANTE_CNTRETENCION.ANO             = RETENCIONES.ANO
AND COMPROBANTE_CNTRETENCION.TIPORETENCION   = RETENCIONES.TIPO
AND COMPROBANTE_CNTRETENCION.CODIGORETENCION = RETENCIONES.CODIGO
WHERE COMPROBANTE_CNTRETENCION.ANO          = s$ano$s
AND COMPROBANTE_CNTRETENCION.TIPO         = 's$tipoCpte$s'
AND COMPROBANTE_CNTRETENCION.NUMERO = $P{PR_NUMCOMP}]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, 'calarcon' CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);

