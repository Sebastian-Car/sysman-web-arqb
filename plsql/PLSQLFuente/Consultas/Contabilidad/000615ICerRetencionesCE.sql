WITH TMPCERTIFICADOS AS 
(
SELECT DETALLE_COMPROBANTE_CNT.TIPO_CPTE,
       DETALLE_COMPROBANTE_CNT.COMPROBANTE,
       DETALLE_COMPROBANTE_CNT.TERCERO TERCERO,
       TERCERO.NOMBRE                  TERCERO_NOMBRE,
       DETALLE_COMPROBANTE_CNT.CUENTA,
       SUM(NVL(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO,0))  VALOR_DEBITO,
       SUM(NVL(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO,0)) VALOR_CREDITO,
       SUM(NVL(DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE,0)) BASE_GRAVABLE,
       TERCERO.DIRECCION ,
       DETALLE_COMPROBANTE_CNT.COMPANIA,
       DETALLE_COMPROBANTE_CNT.ANO, 
       PLAN_CONTABLE.NOMBRE NOMBRECUENTA,
       PLAN_CONTABLE.CLASECUENTA,
       s$codigoPostal$s CODIGOPOSTAL
FROM DETALLE_COMPROBANTE_CNT
  INNER JOIN TERCERO
	  ON  DETALLE_COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA
	  AND DETALLE_COMPROBANTE_CNT.TERCERO  = TERCERO.NIT
	  AND DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL 
  INNER JOIN TIPO_COMPROBANTE
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA  = TIPO_COMPROBANTE.COMPANIA 
    AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO 
  INNER JOIN PLAN_CONTABLE
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.ANO      = PLAN_CONTABLE.ANO
    AND DETALLE_COMPROBANTE_CNT.CUENTA   = PLAN_CONTABLE.CODIGO
    LEFT JOIN (
SELECT DISTINCT DETALLE_COMPROBANTE_CNT.TIPO_CPTE_AFECT,
    DETALLE_COMPROBANTE_CNT.CMPTE_AFECTADO,
    DETALLE_COMPROBANTE_CNT.COMPANIA,
    DETALLE_COMPROBANTE_CNT.ANO
  FROM PLAN_CONTABLE
  INNER JOIN DETALLE_COMPROBANTE_CNT
  ON PLAN_CONTABLE.COMPANIA = DETALLE_COMPROBANTE_CNT.COMPANIA
  AND PLAN_CONTABLE.ANO     = DETALLE_COMPROBANTE_CNT.ANO
  AND PLAN_CONTABLE.CODIGO  = DETALLE_COMPROBANTE_CNT.CUENTA
  INNER JOIN TIPO_COMPROBANTE
  ON DETALLE_COMPROBANTE_CNT.COMPANIA            = TIPO_COMPROBANTE.COMPANIA
  AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE          = TIPO_COMPROBANTE.CODIGO
  WHERE DETALLE_COMPROBANTE_CNT.TIPO_CPTE_AFECT IN ('COM')
  AND PLAN_CONTABLE.CLASECUENTA                 IN ('P')
  AND TIPO_COMPROBANTE.CLASE_CONTABLE           IN ('T')
  AND DETALLE_COMPROBANTE_CNT.COMPANIA           = s$compania$s 
  AND DETALLE_COMPROBANTE_CNT.ANO                = s$anio$s 
  )  COMANULADOS
ON DETALLE_COMPROBANTE_CNT.COMPANIA        = COMANULADOS.COMPANIA
AND DETALLE_COMPROBANTE_CNT.ANO            = COMANULADOS.ANO
AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE      = COMANULADOS.TIPO_CPTE_AFECT
AND DETALLE_COMPROBANTE_CNT.COMPROBANTE    = COMANULADOS.CMPTE_AFECTADO
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA = s$compania$s 
  AND DETALLE_COMPROBANTE_CNT.ANO      = s$anio$s 
  AND TRUNC(DETALLE_COMPROBANTE_CNT.FECHA)    BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s'
,'DD/MM/YYYY')
  AND s$filtroNitONombre$s             BETWEEN 's$tercero1$s' AND 's$tercero2$s'
  AND DETALLE_COMPROBANTE_CNT.CUENTA   BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  AND TIPO_COMPROBANTE.CLASE_CONTABLE NOT IN('Z','T')
  AND PLAN_CONTABLE.CLASECUENTA           IN ('I')
  AND COMANULADOS.CMPTE_AFECTADO          IS NULL
GROUP BY  DETALLE_COMPROBANTE_CNT.TIPO_CPTE,
          DETALLE_COMPROBANTE_CNT.COMPROBANTE,
          DETALLE_COMPROBANTE_CNT.TERCERO,
          TERCERO.NOMBRE, 
          DETALLE_COMPROBANTE_CNT.CUENTA, 
          TERCERO.DIRECCION, 
          DETALLE_COMPROBANTE_CNT.COMPANIA,
          DETALLE_COMPROBANTE_CNT.ANO, 
          PLAN_CONTABLE.NOMBRE,
          PLAN_CONTABLE.CLASECUENTA,
          s$codigoPostal$s
)
SELECT TMPCERTIFICADOS.COMPANIA,
       TMPCERTIFICADOS.CUENTA CODIGO_CUENTA,
       TMPCERTIFICADOS.TERCERO,
       TMPCERTIFICADOS.NOMBRECUENTA       NOMBRECUENTA,
       SUM(TMPCERTIFICADOS.BASE_GRAVABLE) BASE_GRAVABLE,
       SUM(TMPCERTIFICADOS.VALOR_CREDITO) VALOR_CREDITO,
       TMPCERTIFICADOS.CODIGOPOSTAL,
       TMPCERTIFICADOS.TERCERO_NOMBRE,
       TMPCERTIFICADOS.DIRECCION,
       NVL(SUM(TMPPAGOS.PAGOS),0) PAGOS 
FROM TMPCERTIFICADOS
INNER JOIN (
SELECT DETALLE_COMPROBANTE_CNT.COMPANIA,
       DETALLE_COMPROBANTE_CNT.ANO,
       DETALLE_COMPROBANTE_CNT.TIPO_CPTE,
       DETALLE_COMPROBANTE_CNT.COMPROBANTE,
       NVL(SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO),0)  PAGOS
FROM TIPO_COMPROBANTE
  INNER JOIN DETALLE_COMPROBANTE_CNT
    ON  TIPO_COMPROBANTE.COMPANIA = DETALLE_COMPROBANTE_CNT.COMPANIA
    AND TIPO_COMPROBANTE.CODIGO   = DETALLE_COMPROBANTE_CNT.TIPO_CPTE
  INNER JOIN PLAN_CONTABLE
    ON  DETALLE_COMPROBANTE_CNT.CUENTA   = PLAN_CONTABLE.CODIGO
    AND DETALLE_COMPROBANTE_CNT.ANO      = PLAN_CONTABLE.ANO
    AND DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA
  INNER JOIN TERCERO
    ON  DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL
    AND DETALLE_COMPROBANTE_CNT.TERCERO  = TERCERO.NIT
    AND DETALLE_COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA =  s$compania$s 
  AND TIPO_COMPROBANTE.CLASE_CONTABLE  IN ('P')
  AND DETALLE_COMPROBANTE_CNT.FECHA BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY') 
  AND PLAN_CONTABLE.CLASECUENTA          IN ('P','G','Y','A','R')
  AND s$filtroNitONombre$s BETWEEN 's$tercero1$s' 
                                       AND 's$tercero2$s'
GROUP BY  DETALLE_COMPROBANTE_CNT.COMPANIA,
          DETALLE_COMPROBANTE_CNT.ANO,
          DETALLE_COMPROBANTE_CNT.TIPO_CPTE,
          DETALLE_COMPROBANTE_CNT.COMPROBANTE )TMPPAGOS
    ON  TMPCERTIFICADOS.TIPO_CPTE   = TMPPAGOS.TIPO_CPTE
    AND TMPCERTIFICADOS.COMPROBANTE = TMPPAGOS.COMPROBANTE
    AND TMPCERTIFICADOS.COMPANIA    = TMPPAGOS.COMPANIA
    AND TMPCERTIFICADOS.ANO         = TMPPAGOS.ANO  
WHERE TMPCERTIFICADOS.COMPANIA  = s$compania$s 
  AND TMPCERTIFICADOS.TIPO_CPTE NOT IN ('CIE')
GROUP BY  TMPCERTIFICADOS.COMPANIA,
          TMPCERTIFICADOS.CUENTA,
          TMPCERTIFICADOS.TERCERO, 
          TMPCERTIFICADOS.NOMBRECUENTA,
          TMPCERTIFICADOS.CODIGOPOSTAL,
          TMPCERTIFICADOS.TERCERO_NOMBRE,
          TMPCERTIFICADOS.DIRECCION,
          TMPCERTIFICADOS.CUENTA,
          TMPCERTIFICADOS.NOMBRECUENTA
