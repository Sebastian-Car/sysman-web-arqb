SELECT 
  DETALLE_COMPROBANTE_CNT.COMPANIA,
  DETALLE_COMPROBANTE_CNT.TIPO_CPTE,
  TIPO_COMPROBANTE.NOMBRE AS NOMBRE_COMP,
  DETALLE_COMPROBANTE_CNT.COMPROBANTE,
  DETALLE_COMPROBANTE_CNT.CUENTA AS CODIGO_CUENTA,
  PLAN_CONTABLE.NOMBRE           AS NOMBRE_CONT,
  TO_CHAR(DETALLE_COMPROBANTE_CNT.FECHA, 'DD/MM/YYYY' ) FECHA,
  PLAN_PRESUPUESTAL.CODIGO,
  PLAN_PRESUPUESTAL.NOMBRE AS NOMBRE_PRE,
  TERCERO.NOMBRE           AS NOMBRE_TER,
  CASE
    WHEN DETALLE_COMPROBANTE_CNT.VALOR_CREDITO IN (0)
    THEN DETALLE_COMPROBANTE_CNT.VALOR_DEBITO   *(-1)
    ELSE DETALLE_COMPROBANTE_CNT.VALOR_CREDITO
  END VALORINGRESOS
FROM DETALLE_COMPROBANTE_CNT
  INNER JOIN PLAN_CONTABLE
    ON DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.ANO     = PLAN_CONTABLE.ANO
    AND DETALLE_COMPROBANTE_CNT.CUENTA  = PLAN_CONTABLE.CODIGO
  INNER JOIN TIPO_COMPROBANTE
    ON DETALLE_COMPROBANTE_CNT.COMPANIA   = TIPO_COMPROBANTE.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
  LEFT JOIN PLAN_PRESUPUESTAL
    ON PLAN_CONTABLE.COMPANIA      = PLAN_PRESUPUESTAL.COMPANIA
    AND PLAN_CONTABLE.ANO          = PLAN_PRESUPUESTAL.ANO
    AND PLAN_CONTABLE.CUENTA_PPTAL = PLAN_PRESUPUESTAL.CODIGO
  INNER JOIN TERCERO
    ON DETALLE_COMPROBANTE_CNT.COMPANIA             = TERCERO.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.TERCERO            = TERCERO.NIT
    AND DETALLE_COMPROBANTE_CNT.SUCURSAL           = TERCERO.SUCURSAL
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA     = s$compania$s
  AND TRUNC(DETALLE_COMPROBANTE_CNT.FECHA) = s$fecha$s
  AND TIPO_COMPROBANTE.CLASE_CONTABLE  IN('S','I','B')
  AND PLAN_CONTABLE.CLASECUENTA NOT IN ('J','B')
ORDER BY 
  DETALLE_COMPROBANTE_CNT.ANO,
  DETALLE_COMPROBANTE_CNT.TIPO_CPTE,
  DETALLE_COMPROBANTE_CNT.COMPROBANTE,
  DETALLE_COMPROBANTE_CNT.CONSECUTIVO
