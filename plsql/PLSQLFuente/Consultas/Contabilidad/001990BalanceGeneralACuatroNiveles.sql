WITH BALANCEGENERALNIVELES AS (
SELECT PLAN_CONTABLE.COMPANIA,
  PLAN_CONTABLE.ANO,
  PLAN_CONTABLE.ID,
  PLAN_CONTABLE.CODIGO,
  PLAN_CONTABLE.NOMBRE,
  PLAN_CONTABLE.SALDOs$mes$s       SALDO,
  SUBSTR(PLAN_CONTABLE.CODIGO,1,1) CLASE,
  SUBSTR(PLAN_CONTABLE.CODIGO,1,2) GRUPO,
  SUBSTR(PLAN_CONTABLE.CODIGO,1,4) CUENTA,
  PLAN_CONTABLE.TERCERO,
  PLAN_CONTABLE.SUCURSAL,
  PLAN_CONTABLE.AUXILIAR,
  PLAN_CONTABLE.CENTRO_COSTO,
  LENGTH(PLAN_CONTABLE.CODIGO) AS TAMANO,
  PLAN_CONTABLE.MOVIMIENTO,
  PLAN_CONTABLE.MAN_CEN_CTO,
  PLAN_CONTABLE.MAN_AUX_TER,
  PLAN_CONTABLE.MAN_AUX_GEN,
  PLAN_CONTABLE.NATURALEZA
FROM V_PLAN_CONTABLE PLAN_CONTABLE
WHERE PLAN_CONTABLE.COMPANIA = s$compania$s
AND PLAN_CONTABLE.ANO       = s$anio$s
AND PLAN_CONTABLE.CODIGO BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
AND ROUND(PLAN_CONTABLE.SALDOs$mes$s,0)<>0
AND LENGTH(PLAN_CONTABLE.ID)          = 6
ORDER BY PLAN_CONTABLE.COMPANIA,
  PLAN_CONTABLE.ANO,
  PLAN_CONTABLE.ID
)
SELECT BALANCEGENERALNIVELES.COMPANIA,
  BALANCEGENERALNIVELES.ANO,
  s$mes$s MES,
  BALANCEGENERALNIVELES.ID,
  BALANCEGENERALNIVELES.CODIGO,
  BALANCEGENERALNIVELES.NOMBRE  NOMBREID,
  BALANCEGENERALNIVELES.SALDO,
  BALANCEGENERALNIVELES.CLASE,
  BALANCEGENERALNIVELES.GRUPO,
  BALANCEGENERALNIVELES.CUENTA,
  ' ' SUBCUENTA,
  ' ' SUBCUENTA1,
  PLAN_CONTABLE.NOMBRE    NOMBRECLASE,
  PLAN_CONTABLE_1.NOMBRE  NOMBREGRUPO,
  PLAN_CONTABLE_2.NOMBRE  NOMBRECUENTA,
  ' ' NOMBRESUBCUENTA,
  ' ' NOMBRESUBCUENTA1,
  CASE
    WHEN PLAN_CONTABLE.CODIGO='0'
    THEN 'z'
      || PLAN_CONTABLE.CODIGO
    ELSE PLAN_CONTABLE.CODIGO
  END ORDEN
FROM BALANCEGENERALNIVELES
LEFT JOIN V_PLAN_CONTABLE PLAN_CONTABLE
ON BALANCEGENERALNIVELES.COMPANIA = PLAN_CONTABLE.COMPANIA
AND BALANCEGENERALNIVELES.ANO     = PLAN_CONTABLE.ANO
AND BALANCEGENERALNIVELES.CLASE   = PLAN_CONTABLE.ID
LEFT JOIN V_PLAN_CONTABLE  PLAN_CONTABLE_1
ON BALANCEGENERALNIVELES.COMPANIA = PLAN_CONTABLE_1.COMPANIA
AND BALANCEGENERALNIVELES.ANO      = PLAN_CONTABLE_1.ANO
AND BALANCEGENERALNIVELES.GRUPO     = PLAN_CONTABLE_1.ID
LEFT JOIN V_PLAN_CONTABLE  PLAN_CONTABLE_2
ON BALANCEGENERALNIVELES.COMPANIA = PLAN_CONTABLE_2.COMPANIA
AND BALANCEGENERALNIVELES.ANO     = PLAN_CONTABLE_2.ANO
AND BALANCEGENERALNIVELES.CUENTA  = PLAN_CONTABLE_2.ID
