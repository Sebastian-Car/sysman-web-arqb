SELECT  COMPROBANTE_CNT.TIPO, 
        COMPROBANTE_CNT.NUMERO, 
        COMPROBANTE_CNT.FECHA, 
        COMPROBANTE_CNT.VLRAGIRAR, 
        SUM(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO - DETALLE_COMPROBANTE_CNT.VALOR_DEBITO) AS CXP,
        (VLRAGIRAR - SUM(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO - DETALLE_COMPROBANTE_CNT.VALOR_DEBITO)) AS DIFERENCIA, 
        TIPO_COMPROBANTE.NOMBRE AS NOMBRECOMP
FROM COMPROBANTE_CNT 
    INNER JOIN DETALLE_COMPROBANTE_CNT 
        ON COMPROBANTE_CNT.COMPANIA = DETALLE_COMPROBANTE_CNT.COMPANIA
        AND COMPROBANTE_CNT.ANO       = DETALLE_COMPROBANTE_CNT.ANO 
        AND COMPROBANTE_CNT.TIPO      = DETALLE_COMPROBANTE_CNT.TIPO_CPTE 
        AND COMPROBANTE_CNT.NUMERO  = DETALLE_COMPROBANTE_CNT.COMPROBANTE 
    LEFT JOIN V_PLAN_CONTABLE PLAN_CONTABLE
        ON DETALLE_COMPROBANTE_CNT.COMPANIA     = PLAN_CONTABLE.COMPANIA 
        AND DETALLE_COMPROBANTE_CNT.ANO         = PLAN_CONTABLE.ANO
        AND DETALLE_COMPROBANTE_CNT.CUENTA      = PLAN_CONTABLE.ID
    INNER JOIN TIPO_COMPROBANTE 
        ON COMPROBANTE_CNT.COMPANIA     = TIPO_COMPROBANTE.COMPANIA
        AND COMPROBANTE_CNT.TIPO          = TIPO_COMPROBANTE.CODIGO
WHERE COMPROBANTE_CNT.COMPANIA = 001
    AND COMPROBANTE_CNT.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
    AND TIPO_COMPROBANTE.CLASE_CONTABLE IN ('P')
    AND(
        (
            SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA,0,1) = '1' 
            AND SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA,0,2) >= '15' 
            AND PLAN_CONTABLE.CLASECUENTA NOT IN ('I','X','Y','C') 
            AND DETALLE_COMPROBANTE_CNT.VALOR_CREDITO NOT IN (0)
        )
        
    OR
        (   
            SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA,0,1) = '2' 
            AND PLAN_CONTABLE.CLASECUENTA NOT IN ('I','X','Y') 
            AND DETALLE_COMPROBANTE_CNT.VALOR_CREDITO NOT IN (0) 
        )   
    OR 
        (
      SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA,0,1) = '2' 
            AND PLAN_CONTABLE.CLASECUENTA IS NULL 
            AND DETALLE_COMPROBANTE_CNT.VALOR_CREDITO NOT IN (0) 
        )
    OR  
        (
            SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA,0,1) = '2' 
            AND PLAN_CONTABLE.CLASECUENTA NOT IN ('I','X','Y')
            AND DETALLE_COMPROBANTE_CNT.VALOR_DEBITO NOT IN (0) 
        )
    OR 
        (   
            SUBSTR(DETALLE_COMPROBANTE_CNT.CUENTA,0,1) = '2' 
            AND PLAN_CONTABLE.CLASECUENTA IS NULL 
            AND DETALLE_COMPROBANTE_CNT.VALOR_DEBITO NOT IN (0)
        )  
    )  
GROUP BY  COMPROBANTE_CNT.TIPO, 
          COMPROBANTE_CNT.NUMERO, 
          COMPROBANTE_CNT.FECHA, 
          COMPROBANTE_CNT.VLRAGIRAR,
          TIPO_COMPROBANTE.NOMBRE
HAVING (VLRAGIRAR - SUM(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO - DETALLE_COMPROBANTE_CNT.VALOR_DEBITO)) NOT IN (0)
