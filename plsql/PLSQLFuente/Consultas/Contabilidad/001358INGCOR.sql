SELECT VDT.COMPANIA
  , VDT.ANO
  , VDT.TIPO_CPTE
  , VDT.COMPROBANTE
  , VDT.TIPO_COMPROBANTE_NOM
  , VDT.COMPROBANTE
  , VDT.TERCERO_COM_NOM TERCERO_NOM
  , VDT.TERCERO_COM  TERCERO
  , VDT.DIRECCION_DET  DIRECCION
  , VDT.CODIGOPOSTAL
  , VDT.FECHA
  , VDT.CONTRIBUYENTE
  , VDT.DESCRIPCION
  , VDT.TEXTO
  , VDT.CUENTAPPTAL
  , VDT.CODIGO_CUENTA
  , VDT.CUENTANOMBRE
  , VDT.VALOR_DEBITO
  , VDT.VALOR_CREDITO
  , VDT.NRO_DOCUMENTO
  , VDT.FECHA_VCN_DOC
  , VDT.VLRAGIRAR
  , CASE WHEN VALOR_DEBITO <> 0 THEN 'A' || CODIGO_CUENTA ELSE 'Z' || CODIGO_CUENTA END SEC
  , VDT.CREATED_BY,
  NVL(USUARIO.NOMBRE1,'') ||' '||
  NVL(USUARIO.NOMBRE2,'') ||' '||
  NVL(USUARIO.APELLIDO1,'') ||' '||
  NVL(USUARIO.APELLIDO2,'')NOMBREUSUARIO
FROM V_DETALLE_AUXILIAR_CNT VDT
LEFT JOIN USUARIO
ON VDT.CREATED_BY  = USUARIO.CODIGO
WHERE COMPANIA = s$compania$s
AND ANO = s$ano$s 
AND TIPO_CPTE = 's$tipoCpte$s'
AND COMPROBANTE BETWEEN  s$numeroPptoInicial$s AND s$numeroPptoFinal$s