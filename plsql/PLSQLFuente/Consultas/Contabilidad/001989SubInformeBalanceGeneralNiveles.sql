SELECT PLAN_CONTABLE.COMPANIA,
  PLAN_CONTABLE.ANO,
  PLAN_CONTABLE.CODIGO ID,
  PLAN_CONTABLE.NOMBRE,
  PLAN_CONTABLE.SALDO$P!{PR_MES} AS SALDO,
  PLAN_CONTABLE.NATURALEZA,
  CASE
    WHEN CODIGO='0'
    THEN 'z'
      || PLAN_CONTABLE.CODIGO
    ELSE PLAN_CONTABLE.CODIGO
  END ORDEN,
  CASE
    WHEN NATURALEZA='D'
    THEN
      CASE
        WHEN PLAN_CONTABLE.SALDO$P!{PR_MES}>=0
        THEN PLAN_CONTABLE.SALDO$P!{PR_MES}
        ELSE 0
      END
    ELSE
      CASE
        WHEN PLAN_CONTABLE.SALDO$P!{PR_MES} >=0
        THEN 0
        ELSE -PLAN_CONTABLE.SALDO$P!{PR_MES}
      END
  END SALDODEBITO,
  CASE
    WHEN NATURALEZA='C'
    THEN
      CASE
        WHEN PLAN_CONTABLE.SALDO$P!{PR_MES}>=0
        THEN PLAN_CONTABLE.SALDO$P!{PR_MES}
        ELSE 0
      END
    ELSE
      CASE
        WHEN PLAN_CONTABLE.SALDO$P!{PR_MES} >=0
        THEN 0
        ELSE -PLAN_CONTABLE.SALDO$P!{PR_MES}
      END
  END SALDOCREDITO
FROM PLAN_CONTABLE
WHERE PLAN_CONTABLE.COMPANIA = '$P!{PR_COMPANIA}'
 AND PLAN_CONTABLE.ANO        = $P!{PR_ANO}
 AND PLAN_CONTABLE.CODIGO BETWEEN '$P!{PR_CODIGOINI}' AND '$P!{PR_CODIGOFIN}' 
 AND LENGTH(CODIGO)           = 1
 AND ROUND(SALDO$P!{PR_MES} ,0) <> 0
ORDER BY PLAN_CONTABLE.COMPANIA,
  PLAN_CONTABLE.ANO,
  PLAN_CONTABLE.CODIGO
