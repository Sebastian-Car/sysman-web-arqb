MERGE INTO CONSULTAS FIN USING (SELECT '002396FlujoEfectivo' INFORME ,TO_CLOB(q'[SELECT
    SUM(SANTERIOR) SANTERIOR,
    SUM(NUEVO) SNUEVO
FROM
    (
        SELECT
            PLAN_FLUJO_EFECTIVO_CGN.ACTIVIDAD,
            PLAN_FLUJO_EFECTIVO_CGN.TIPO_EFECTIVO,
            PLAN_FLUJO_EFECTIVO_CGN.NOMBRE   AS NOMBRECONCEPTO,
            PLAN_CONTABLE.CLASECUENTA,
            SALDOs$mesInicial$s              SANTERIOR,
            0 NUEVO
        FROM
            ( PLAN_CONTABLE
            INNER JOIN PLAN_FLUJO_EFECTIVO_CGN ON PLAN_FLUJO_EFECTIVO_CGN.COMPANIA = PLAN_CONTABLE.COMPANIA
                                                  AND PLAN_FLUJO_EFECTIVO_CGN.ANO = PLAN_CONTABLE.ANO
                                                  AND PLAN_FLUJO_EFECTIVO_CGN.CODIGO = PLAN_CONTABLE.CONCEPTO_FLUJO_CGN )
        WHERE
            PLAN_CONTABLE.ANO = s$anioInicial$s
        GROUP BY
            PLAN_FLUJO_EFECTIVO_CGN.ACTIVIDAD,
            PLAN_FLUJO_EFECTIVO_CGN.TIPO_EFECTIVO,
            PLAN_FLUJO_EFECTIVO_CGN.NOMBRE,
            PLAN_CONTABLE.CLASECUENTA,
            SALDOs$mesInicial$s
        UNION ALL
        SELECT
            PLAN_FLUJO_EFECTIVO_CGN.ACTIVIDAD,
            PLAN_FLUJO_EFECTIVO_CGN.TIPO_EFECTIVO,
            PLAN_FLUJO_EFECTIVO_CGN.NOMBRE   AS NOMBRECONCEPTO,
            PLAN_CONTABLE.CLASECUENTA,
            0 NUEVO,
            SALDOs$mesFinal$s              SANTERIOR
        FROM
            ( PLAN_CONTABLE
            INNER JOIN PLAN_FLUJO_EFECTIVO_CGN ON PLAN_FLUJO_EFECTIVO_CGN.COMPANIA = PLAN_CONTABLE.COMPANIA
                                                  AND PLAN_FLUJO_EFECTIVO_CGN.ANO = PLAN_CONTABLE.ANO
                                                  AND PLAN_FLUJO_EFECTIVO_CGN.CODIGO = PLAN_CONTABLE.CONCEPTO_FLUJO_CGN )
        WHERE
            PLAN_CONTABLE.ANO = s$anioFinal$s
        GROUP BY
            PLAN_FLUJO_EFECTIVO_CGN.ACTIVIDAD,
            PLAN_FLUJO_EFECTIVO_CGN.TIPO_EFECTIVO,
            PLAN_FLUJO_EFECTIVO_CGN.NOMBRE,
            PLAN_CONTABLE.CLASECUENTA,
            SALDOs$mesFinal$s
    )
WHERE
    ACTIVIDAD = 'O'
    AND TIPO_EFECTIVO = 'G'
GROUP BY
    ACTIVIDAD]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);