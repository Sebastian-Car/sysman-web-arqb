SELECT COMPROBANTE_CNT.COMPANIA, 
       COMPROBANTE_CNT.ANO, 
       COMPROBANTE_CNT.CUENTA, 
       COMPROBANTE_CNT.NRO_DOCUMENTO, 
       COMPROBANTE_CNT.CUENTABANCO,
       COMPROBANTE_CNT.TERCERO AS CEDULATERCERO, 
       TERCERO.NOMBRE AS NOMBRETERCERO, 
       SUM(COMPROBANTE_CNT.VLRAGIRAR) AS TOTALVALORAGIRAR, 
       PLAN_CONTABLE.NOMBRE AS NOMBREBANCO,
       COMPROBANTE_CNT.FECHA
FROM COMPROBANTE_CNT INNER JOIN TERCERO ON COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA AND COMPROBANTE_CNT.TERCERO = TERCERO.NIT AND COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL     
      INNER JOIN TIPO_COMPROBANTE ON COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA AND COMPROBANTE_CNT.TIPO = TIPO_COMPROBANTE.CODIGO       
      LEFT JOIN V_PLAN_CONTABLE PLAN_CONTABLE ON COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA AND COMPROBANTE_CNT.CUENTA = PLAN_CONTABLE.ID AND COMPROBANTE_CNT.ANO = PLAN_CONTABLE.ANO 
WHERE TIPO_COMPROBANTE.CLASE_CONTABLE IN ('E')   
  AND COMPROBANTE_CNT.COMPANIA      = s$compania$s 
  AND COMPROBANTE_CNT.ANO           = s$anio$s  
  AND COMPROBANTE_CNT.CUENTA        = 's$cuenta$s' 
  AND COMPROBANTE_CNT.NRO_DOCUMENTO = 's$nroDocumento$s'  
GROUP BY COMPROBANTE_CNT.COMPANIA, COMPROBANTE_CNT.ANO, COMPROBANTE_CNT.CUENTA, COMPROBANTE_CNT.NRO_DOCUMENTO, COMPROBANTE_CNT.CUENTABANCO, COMPROBANTE_CNT.TERCERO, TERCERO.NOMBRE, PLAN_CONTABLE.NOMBRE, COMPROBANTE_CNT.FECHA
ORDER BY COMPROBANTE_CNT.COMPANIA, COMPROBANTE_CNT.ANO
