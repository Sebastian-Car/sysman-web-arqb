         PLAN_CONTABLE.NOMBRE,
         SALDOAUX.COMPANIA, 
         SALDOAUX.TERCERO,
         CASE WHEN SUBSTR(SALDOAUX.CODIGO,1,1)='0'
              THEN 'z'||SALDOAUX.CODIGO
              ELSE SALDOAUX.CODIGO
              END,
            CASE WHEN LENGTH(SALDOAUX.CODIGO) = s$digitos$s THEN NVL(CENTRO_COSTO_P.CODIGO,'99999999999999999999') ELSE CENTRO_COSTO_P.CODIGO END,
            SALDOAUX.REFERENCIA, 
            SALDOAUX.AUXILIAR,
            SALDOAUX.FUENTE_RECURSOS)
        ]') || TO_CLOB(q'[      ORDER BY CODIGO, CENTRO_COSTO]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
        
