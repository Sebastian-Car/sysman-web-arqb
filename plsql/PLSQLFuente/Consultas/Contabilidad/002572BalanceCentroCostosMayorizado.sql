            SALDOAUX.REFERENCIA, 
            SALDOAUX.AUXILIAR,
            SALDOAUX.FUENTE_RECURSOS    
 FROM (s$baseAuxiliar$s) SALDOAUX 
        LEFT JOIN (SELECT COMPANIA, ANO, CODIGO, NOMBRE 
                FROM CENTRO_COSTO
                WHERE MOVIMIENTO = 0
                AND ANO = s$anio$s) CENTRO_COSTO_P
        ON SALDOAUX.COMPANIA = CENTRO_COSTO_P.COMPANIA
        AND SALDOAUX.ANO = CENTRO_COSTO_P.ANO
        AND SUBSTR(SALDOAUX.CENTRO_COSTO, 1, LENGTH(CENTRO_COSTO_P.CODIGO)) = CENTRO_COSTO_P.CODIGO 
       LEFT JOIN PLAN_CONTABLE
    ON PLAN_CONTABLE.COMPANIA = SALDOAUX.COMPANIA
    AND PLAN_CONTABLE.ANO = SALDOAUX.ANO 
    AND PLAN_CONTABLE.CODIGO = SALDOAUX.CODIGO
 WHERE LENGTH(SALDOAUX.CODIGO) <= s$digitos$s
    AND (SALDOAUX.DEBITO         + SALDOAUX.CREDITO 
       + ABS(SALDOAUX.SALDOANT)  + ABS(SALDOAUX.SALDOFIN) 
       ) <>
       CASE WHEN s$saldoCero$s = 0
       THEN 0
       ELSE SALDOAUX.DEBITO         + SALDOAUX.CREDITO 
          + ABS(SALDOAUX.SALDOANT)  + ABS(SALDOAUX.SALDOFIN)
          + 1 
       END 
    GROUP BY SUBSTR(SALDOAUX.CODIGO,1,1), 
         SALDOAUX.CODIGO, 
