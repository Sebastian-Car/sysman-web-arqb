                 OR (SALDOAUX.NATURALEZA = 'C' AND (SALDOAUX.SALDOANT)<0)   
               THEN ABS(SALDOAUX.SALDOANT)
               ELSE 0 END) SALDO]') || TO_CLOB(q'[ANTDEBITO,
          SUM(CASE WHEN (SALDOAUX.NATURALEZA = 'C' AND (SALDOAUX.SALDOANT)>=0) 
                 OR (SALDOAUX.NATURALEZA = 'D' AND (SALDOAUX.SALDOANT)<0)   
               THEN ABS(SALDOAUX.SALDOANT)
               ELSE 0 END) SALDOANTCREDITO,        
          SUM(CASE WHEN (SALDOAUX.NATURALEZA = 'D' AND (SALDOAUX.SALDOFIN)>=0) 
                 OR (SALDOAUX.NATURALEZA = 'C' AND (SALDOAUX.SALDOFIN)<0)   
               THEN ABS(SALDOAUX.SALDOFIN)
               ELSE 0 END) SALDONUEDEBITO,
          SUM(CASE WHEN (SALDOAUX.NATURALEZA = 'C' AND (SALDOAUX.SALDOFIN)>=0) 
                 OR (SALDOAUX.NATURALEZA = 'D' AND (SALDOAUX.SALDOFIN)<0)   
               THEN ABS(SALDOAUX.SALDOFIN)
               ELSE 0 END) SALDONUECREDITO, 
         SUBSTR((CASE WHEN SUBSTR(SALDOAUX.CODIGO,1,1)='0'
              THEN 'z'||SALDOAUX.CODIGO
              ELSE SALDOAUX.CODIGO
              END),1,1) AS CLASEORDEN,
         '' ORDENCUENTAS,
            CASE WHEN SUBSTR(SALDOAUX.CODIGO,1,1)='0'
              THEN 'z'||SALDOAUX.CODIGO
              ELSE SALDOAUX.CODIGO
              END AS ORDEN,
