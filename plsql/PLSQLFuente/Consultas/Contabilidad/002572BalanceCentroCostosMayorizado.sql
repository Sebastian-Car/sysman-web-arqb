MERGE INTO CONSULTAS FIN USING (SELECT '002572BalanceCentroCostosMayorizado' INFORME ,TO_CLOB(q'[SELECT CLASE, CODIGO, DEBITO, CREDITO, NOMBRE, COMPANIA, TERCERO, SALDOANTDEBITO, SALDOANTCREDITO, SALDONUEDEBITO, 
