         SALDOAUX.CODIGO, 
         SALDOAUX.DEBITO, 
         SALDOAUX.CREDITO, 
         PLAN_CONTABLE.NOMBRE,
         SALDOAUX.COMPANIA, 
         SALDOAUX.TERCERO,
         CASE WHEN (SALDOAUX.NATURALEZA = 'D' AND (SALDOAUX.SALDOANT)>=0) 
                 OR (SALDOAUX.NATURALEZA = 'C' AND (SALDOAUX.SALDOANT)<0)   
               THEN ABS(SALDOAUX.SALDOANT)
               ELSE 0 END SALDOANTDEBITO,
          CASE WHEN (SALDOAUX.NATURALEZA = 'C' AND (SALDOAUX.SALDOANT)>=0) 
                 OR (SALDOAUX.NATURALEZA = 'D' AND (SALDOAUX.SALDOANT)<0)   
               THEN ABS(SALDOAUX.SALDOANT)
               ELSE 0 END SALDOANTCREDITO,        
          CASE WHEN (SALDOAUX.NATURALEZA = 'D' AND (SALDOAUX.SALDOFIN)>=0) 
                 OR (SALDOAUX.NATURALEZA = 'C' AND (SALDOAUX.SALDOFIN)<0)   
               THEN ABS(SALDOAUX.SALDOFIN)
               ELSE 0 END SALDONUEDEBITO,
          CASE WHEN (SALDOAUX.NATURALEZA = 'C' AND (SALDOAUX.SALDOFIN)>=0) 
                 OR (SALDOAUX.NATURALEZA = 'D' AND (SALDOAUX.SALDOFIN)<0)   
               THEN ABS(SALDOAUX.SALDOFIN)
               ELSE 0 END SALDONUECREDITO, 
         SUBSTR((CASE WHEN SUBSTR(SALDOAUX.CODIGO,1,1)='0'
              THEN 'z'||SALDOAUX.CODIGO
              ELSE SALDOAUX.CODIGO
              END),1,1) AS CLASEORDEN,
         '' ORDENCUENTAS,
            CASE WHEN SUBSTR(SALDOAUX.CODIGO,1,1)='0'
              THEN 'z'||SALDOAUX.CODIGO
              ELSE SALDOAUX.CODIGO
              END AS ORDEN,
            SALDOAUX.CENTRO_COSTO,
            SALDOAUX.REFERENCIA, 
            SALDOAUX.AUXILIAR,
            SALDOAUX.FUENTE_RECURSOS    
FROM (s$baseAuxiliar$s) SALDOAUX 
        LEFT JOIN PLAN_CONTABLE
    ON PLAN_CONTABLE.COMPANIA = SALDOAUX.COMPANIA
    AND PLAN_CONTABLE.ANO = SALDOAUX.ANO 
    AND PLAN_CONTABLE.CODIGO = SALDOAUX.CODIGO
 WHERE LENGTH(SALDOAUX.CODIGO) <= s$digitos$s
   AND (SALDOAUX.DEBITO         + SALDOAUX.CREDITO 
      + ABS(SALDOAUX.SALDOANT)  + ABS(SALDOAUX.SALDOFIN) 
      ) <>
      CASE WHEN s$saldoCero$s = 0
      THEN 0
      ELSE SALDOAUX.DEBITO         + SALDOAUX.CREDITO 
         + ABS(SALDOAUX.SALDOANT)  + ABS(SALDOAUX.SALDOFIN)
         + 1 
      END 

              UNION 
            
SELECT  SUBSTR(SALDOAUX.CODIGO,1,1) CLASE, 
         SALDOAUX.CODIGO, 
         SALDOAUX.DEBITO, 
