SELECT 
  VDT.COMPANIA AS COMP
  , VDT.TIPO_CPTE AS TIPOCOMP
  , VDT.COMPROBANTE AS NUMCOMP
  , VDT.COMPROBANTE AS NUMERO 
  , VDT.TERCERO_COM_NOM AS NOMBRE
  , VDT.TERCERO_COM  AS TERCERO
  , VDT.DIRECCION_DET AS DIRECCION1
  , VDT.TELEFONOS AS TELEFONOS1
  , CASE
    WHEN TO_NUMBER(VDT.VLRAGIRAR) = 1
    THEN 0
    ELSE VDT.VLRAGIRAR
  END SUMADE
  , TO_CHAR(VDT.FECHA,'DD/MM/YYYY') FECHA
  , VDT.CENTRO_COSTO
  , VDT.VLR_DOCUMENTO
  , VDT.CONTRIBUYENTE
  , VDT.DESCRIPCION
  , VDT.TEXTO
  , VDT.CUENTAPPTAL
  , VDT.CODIGO_CUENTA_DET AS CODIGO_CUENTA
  , VDT.CUENTANOMBRE AS TOTALNOMBRE
  , VDT.VALOR_DEBITO
  , VDT.VALOR_CREDITO
  , VDT.NRO_DOCUMENTO 
  , VDT.NRO_DOCUMENTO_DET AS NRO_DOCUMENTO_DCNT
  , VDT.FECHA_VCN_DOC
  , VDT.VLRAGIRAR
  , CASE WHEN VALOR_DEBITO <> 0 THEN 'A' || CODIGO_CUENTA ELSE 'Z' || CODIGO_CUENTA END SEC
  , VDT.CREATED_BY AS ELABORO
  , MAX(TERCEROPAGOS.CUENTA)  KEEP (DENSE_RANK LAST ORDER BY ROWNUM)  CUENTA_TERCERO
  , MAX(TERCEROPAGOS.BANCO)  KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NOM_BANCO
  , MAX(BANCO.NOMBREBANCO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NOMBRE_BANCO   
  , VDT.CENTRO_COSTO AS CENTRO
  , VDT.ANO ANOCOMP
  , VDT.CENTRO_COSTO_VIS || '-' || CENTRO_COSTO.NOMBRE CENTRO_VIS
FROM V_DETALLE_AUXILIAR_CNT VDT
LEFT JOIN TERCEROPAGOS
ON VDT.COMPANIA  = TERCEROPAGOS.COMPANIA
AND VDT.TERCERO  = TERCEROPAGOS.NIT
AND VDT.SUCURSAL = TERCEROPAGOS.SUCURSAL
LEFT JOIN BANCO
  ON TERCEROPAGOS.COMPANIA  = TERCEROPAGOS.COMPANIA
  AND TERCEROPAGOS.BANCO  = BANCO.BANCO
LEFT JOIN CENTRO_COSTO
ON VDT.COMPANIA = CENTRO_COSTO.COMPANIA
AND VDT.ANO = CENTRO_COSTO.ANO
AND VDT.CENTRO_COSTO_VIS = CENTRO_COSTO.CODIGO										  
WHERE VDT.COMPANIA = s$compania$s
AND VDT.ANO = s$ano$s 
AND VDT.TIPO_CPTE = 's$tipoCpte$s'
AND VDT.COMPROBANTE BETWEEN  s$numeroPptoInicial$s AND s$numeroPptoFinal$s
GROUP BY 
VDT.COMPANIA,
VDT.ANO,
VDT.TIPO_CPTE,
VDT.COMPROBANTE,
VDT.TERCERO_COM_NOM,
VDT.TERCERO_COM,
VDT.DIRECCION_DET,
VDT.TELEFONOS,
CASE
    WHEN TO_NUMBER(VDT.VLRAGIRAR) = 1
    THEN 0
    ELSE VDT.VLRAGIRAR
  END,
TO_CHAR(VDT.FECHA,'DD/MM/YYYY'),
VDT.CENTRO_COSTO,
VDT.VLR_DOCUMENTO,
VDT.CONTRIBUYENTE,
VDT.DESCRIPCION,
VDT.TEXTO,
VDT.CUENTAPPTAL,
VDT.CODIGO_CUENTA_DET,
VDT.CUENTANOMBRE,
VDT.VALOR_DEBITO,
VDT.VALOR_CREDITO,
VDT.NRO_DOCUMENTO,
VDT.NRO_DOCUMENTO_DET,
VDT.FECHA_VCN_DOC,
VDT.VLRAGIRAR,
CASE WHEN VALOR_DEBITO <> 0 THEN 'A' || CODIGO_CUENTA ELSE 'Z' || CODIGO_CUENTA END,
VDT.CREATED_BY,
VDT.CENTRO_COSTO_VIS,
CENTRO_COSTO.NOMBRE			   
ORDER BY MAX(TERCEROPAGOS.CUENTA)  KEEP (DENSE_RANK LAST ORDER BY ROWNUM)