SELECT
    TO_CHAR ('s$nombreTipo$s'),
    TERCERO.NOMBRE || ' / ' || PLAN_CONTABLE_1.NOMBRE   NOMBRECOMPLETO,
    SUM(V_DETALLE_AUXILIAR_CNT.VALOR_CREDITO) TOTALRETENCIONES
  FROM V_DETALLE_AUXILIAR_CNT
  INNER JOIN PLAN_CONTABLE  PLAN_CONTABLE_1
  ON V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA = PLAN_CONTABLE_1.CODIGO
  AND V_DETALLE_AUXILIAR_CNT.ANO          = PLAN_CONTABLE_1.ANO
  AND V_DETALLE_AUXILIAR_CNT.COMPANIA     = PLAN_CONTABLE_1.COMPANIA
  INNER JOIN DETALLE_COMPROBANTE_CNT
  ON PLAN_CONTABLE_1.CODIGO            = DETALLE_COMPROBANTE_CNT.CUENTA
  AND PLAN_CONTABLE_1.ANO              = DETALLE_COMPROBANTE_CNT.ANO
  AND PLAN_CONTABLE_1.COMPANIA         = DETALLE_COMPROBANTE_CNT.COMPANIA
  LEFT JOIN TERCERO
  ON V_DETALLE_AUXILIAR_CNT.COMPANIA  = TERCERO.COMPANIA
  AND V_DETALLE_AUXILIAR_CNT.TERCERO  = TERCERO.NIT
  AND V_DETALLE_AUXILIAR_CNT.SUCURSAL = TERCERO.SUCURSAL 
  WHERE    V_DETALLE_AUXILIAR_CNT.COMPANIA = s$compania$s
    AND V_DETALLE_AUXILIAR_CNT.FECHA BETWEEN  s$fechaInicial$s AND s$fechaFinal$s
    AND NVL (V_DETALLE_AUXILIAR_CNT.CUENTA_COMP,'') BETWEEN s$bancoInicial$s AND s$bancoFinal$s
    AND V_DETALLE_AUXILIAR_CNT.CLASE_CONTABLE NOT IN ('P') 
    AND V_DETALLE_AUXILIAR_CNT.CLASECUENTA IN ('I')
  GROUP BY  TERCERO.NOMBRE || ' / ' || PLAN_CONTABLE_1.NOMBRE
