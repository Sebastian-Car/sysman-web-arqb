MERGE INTO CONSULTAS FIN USING (SELECT '002583BalanceGeneralAuxiliares' INFORME ,TO_CLOB(q'[SELECT COMPANIA  
      , SUBSTR(CODIGO, 1,1) CLASE   
      , CODIGO   
      , NOMBRE   
 PLAN_CONTABLE_NOMBRE  
      , CASE WHEN TERCERO IS NULL THEN ''
           ELSE TERCERO END TERCERO  
      , CENTRO_COSTO   
      , AUXILIAR   
      , REFERENCIA   
      , FUENTE_RECURSOS   
      , (CASE WHEN (NATURALEZA='D' AND SALDOs$mestrabajo$s>=0)    
		      THEN SALDOs$mestrabajo$s    
		      ELSE 0    
		  END)+ (CASE WHEN (NATURALEZA='C' AND SALDOs$mestrabajo$s<0)    
		              THEN - SALDOs$mestrabajo$s   
		              ELSE 0    
		        END) SALDONUEDEBITO   
    , (CASE WHEN (NATURALEZA='C' AND SALDOs$mestrabajo$s>=0)    
		      THEN SALDOs$mestrabajo$s    
		      ELSE 0    
		 END)+ (CASE WHEN (NATURALEZA='D' AND SALDOs$mestrabajo$s<0)    
		             THEN -SALDOs$mestrabajo$s    
		             ELSE 0    
		        END) SALDONUECREDITO    
    , CASE WHEN (SUBSTR(CODIGO,1,1)='0')   
			         THEN 'z' || CODIGO   
			         ELSE CODIGO   
			         END CLASEORDEN  
FROM(s$baseBalance$s)  
WHERE LENGTH(CODIGO) <= s$digitos$s 
 s$saldoCeroExt$s
AND CODIGO BETWEEN s$codigoInicial$s AND s$codigoFinal$s]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, 'mrosero' MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);

