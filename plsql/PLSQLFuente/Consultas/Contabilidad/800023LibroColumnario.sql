SELECT 
   FECHA
  ,TIPO_CPTE
  ,TO_CHAR(COMPROBANTE) COMPROBANTE
  ,DESCRIPCION
  ,NOMBRE
  ,s$nvlLibro$s 
FROM (
  SELECT 
     CONSECUTIVO
    ,TO_CHAR(FECHA,'DD/MM/YYYY') FECHA
    ,TIPO_CPTE
    ,COMPROBANTE
    ,DESCRIPCION
    ,NOMBRE
    ,CUENTA
    ,'DEB '                                          || 
     TO_CHAR(SUM(VALOR_DEBITO ),'999,999,999,999.00')|| 
     '  CRE '                                        ||
     TO_CHAR(SUM(VALOR_CREDITO),'999,999,999,999.00') VALOR
  FROM (
    SELECT 
       DETALLE_COMPROBANTE_CNT.FECHA
      ,DETALLE_COMPROBANTE_CNT.TIPO_CPTE
      ,DETALLE_COMPROBANTE_CNT.COMPROBANTE
      ,DETALLE_COMPROBANTE_CNT.CONSECUTIVO
      ,DETALLE_COMPROBANTE_CNT.DESCRIPCION
      ,DETALLE_COMPROBANTE_CNT.CUENTA 
      ,DETALLE_COMPROBANTE_CNT.VALOR_DEBITO
      ,DETALLE_COMPROBANTE_CNT.VALOR_CREDITO
      ,TERCERO.NOMBRE 
    FROM DETALLE_COMPROBANTE_CNT 
      INNER JOIN TERCERO 
         ON DETALLE_COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA
        AND DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL
        AND DETALLE_COMPROBANTE_CNT.TERCERO  = TERCERO.NIT
    WHERE DETALLE_COMPROBANTE_CNT.COMPANIA = s$compania$s
      AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE    BETWEEN s$tipoInicial$s  AND s$tipoFinal$s
      AND TRUNC(DETALLE_COMPROBANTE_CNT.FECHA) BETWEEN s$fechaInicial$s AND s$fechaFinal$s
      AND DETALLE_COMPROBANTE_CNT.VALOR_CREDITO NOT IN(0) 
    ORDER BY 
       DETALLE_COMPROBANTE_CNT.FECHA
      ,DETALLE_COMPROBANTE_CNT.TIPO_CPTE
      ,DETALLE_COMPROBANTE_CNT.COMPROBANTE
      ,DETALLE_COMPROBANTE_CNT.CUENTA 
  )
  GROUP BY 
     CONSECUTIVO
    ,TO_CHAR(FECHA,'DD/MM/YYYY')
    ,TIPO_CPTE
    ,COMPROBANTE
    ,DESCRIPCION
    ,NOMBRE
    ,CUENTA
)
PIVOT(
  MIN(VALOR) FOR CUENTA IN (s$pivot$s)
)
ORDER BY 
   FECHA
  ,TIPO_CPTE
  ,COMPROBANTE
