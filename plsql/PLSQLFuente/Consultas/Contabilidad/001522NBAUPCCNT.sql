SELECT TIPO_COMPROBANTE_NOM,
  COMPROBANTE,
  FECHA_COMPROBANTE,
  TERCERO_COM_NOM TERCERO_NOM,
  TERCERO_COM TERCERO,
  NRO_DOCUMENTO,
  FECHA_VCN_DOC,
  NVL(VLR_DOCUMENTO,0) VLR_DOCUMENTO,
  DESCRIPCION,
  TEXTO,
  NRO_DOCUMENTO_DET,
  CUENTAPPTAL,
  CODIGO_CUENTA_DET,
  CUENTANOMBRE,
  NVL(VALOR_DEBITO,0) VALOR_DEBITO,
  NVL(VALOR_CREDITO,0) VALOR_CREDITO
FROM V_DETALLE_AUXILIAR_CNT
WHERE COMPANIA   = s$compania$s
  AND ANO             = s$ano$s 
  AND TIPO_CPTE  = 's$tipoCpte$s'  
  AND COMPROBANTE BETWEEN s$numeroPptoInicial$s AND s$numeroPptoFinal$s
ORDER BY COMPROBANTE
