SELECT  COMPROBANTE_PPTAL.TIPO, 
        TIPO_COMPROBANTE.NOMBRE AS NOMBRECOMP, 
        COMPROBANTE_PPTAL.NUMERO AS NUMEROCONTABLE, 
        COMPROBANTE_PPTAL.FECHA, 
        COMPROBANTE_PPTAL.TERCERO,
        TERCERO.NOMBRE AS NOMBRETER, 
        COMPROBANTE_PPTAL.DESCRIPCION, 
        COMPROBANTE_PPTAL.NRO_DOCUMENTO,
        COMPROBANTE_PPTAL.VLR_DOCUMENTO AS VALORDOC 
FROM COMPROBANTE_PPTAL 
  LEFT JOIN COMPROBANTE_CNT 
    ON COMPROBANTE_PPTAL.COMPANIA = COMPROBANTE_CNT.COMPANIA  
    AND COMPROBANTE_PPTAL.ANO = COMPROBANTE_CNT.ANO 
    AND COMPROBANTE_PPTAL.TIPO = COMPROBANTE_CNT.TIPO 
    AND COMPROBANTE_PPTAL.NUMERO = COMPROBANTE_CNT.NUMERO 
  INNER JOIN TIPO_COMPROBANTE 
    ON COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBANTE.COMPANIA  
    AND COMPROBANTE_PPTAL.TIPO = TIPO_COMPROBANTE.CODIGO
  INNER JOIN CLASECNT 
    ON TIPO_COMPROBANTE.CLASE_CONTABLE = CLASECNT.CODIGO 
  INNER JOIN TERCERO 
    ON COMPROBANTE_PPTAL.COMPANIA = TERCERO.COMPANIA 
    AND COMPROBANTE_PPTAL.TERCERO = TERCERO.NIT 
    AND COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL 
WHERE COMPROBANTE_PPTAL.COMPANIA = s$compania$s 
  AND CLASETIPOPPTAL IS NOT NULL 
  AND COMPROBANTE_PPTAL.TIPO BETWEEN s$tipoInicial$s AND s$tipoFinal$s 
  AND (COMPROBANTE_CNT.NUMERO =0 OR COMPROBANTE_CNT.NUMERO IS NULL) 
  AND COMPROBANTE_PPTAL.NUMERO IS NOT NULL 
  AND COMPROBANTE_PPTAL.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
