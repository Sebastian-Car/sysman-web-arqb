SELECT 
  DETALLE_COMPROBANTE_CNT.COMPANIA,
  V_PLAN_CONTABLE.NOMBRE,
  V_PLAN_CONTABLE.SALDOs$mesAnterior$s SALDOINICIAL,
  MAX(TIPO_COMPROBANTE.NOMBRE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) ULTIMODENOMBRE,
  SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO) SUMADEVALOR_DEBITO, 
  SUM(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO) SUMADEVALOR_CREDITO, 
  DETALLE_COMPROBANTE_CNT.FECHA, 
  DETALLE_COMPROBANTE_CNT.TIPO_CPTE
FROM DETALLE_COMPROBANTE_CNT 
  LEFT JOIN V_PLAN_CONTABLE 
     ON DETALLE_COMPROBANTE_CNT.COMPANIA = V_PLAN_CONTABLE.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.ANO = V_PLAN_CONTABLE.ANO
    AND DETALLE_COMPROBANTE_CNT.CUENTA = V_PLAN_CONTABLE.CODIGO
    AND DETALLE_COMPROBANTE_CNT.ID = V_PLAN_CONTABLE.ID
  LEFT JOIN TIPO_COMPROBANTE 
     ON DETALLE_COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA = s$compania$s
  AND DETALLE_COMPROBANTE_CNT.ANO = s$anio$s
  AND EXTRACT (MONTH FROM DETALLE_COMPROBANTE_CNT.FECHA)= s$mes$s
  AND TIPO_COMPROBANTE.CLASE_CONTABLE IN ('I','B','E','S','G','J')
  AND DETALLE_COMPROBANTE_CNT.CUENTA = 's$cuentaCaja$s' 
GROUP BY 
  DETALLE_COMPROBANTE_CNT.COMPANIA, V_PLAN_CONTABLE.NOMBRE, 
  DETALLE_COMPROBANTE_CNT.FECHA, DETALLE_COMPROBANTE_CNT.TIPO_CPTE
, V_PLAN_CONTABLE.SALDOs$mesAnterior$s
