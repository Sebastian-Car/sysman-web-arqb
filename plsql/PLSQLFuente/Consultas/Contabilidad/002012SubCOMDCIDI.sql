SELECT DISTINCT DETALLE_COMPROBANTE_PPTAL.COMPANIA COMP,
  DETALLE_COMPROBANTE_PPTAL.ANO ANOCOMP,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE TIPOCOMP,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE NUMCOMP,
  DC_PPTAL.CMPTE_AFECTADO NUMDISP,
  DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO NUMRESERVA,
  DETALLE_COMPROBANTE_PPTAL.CUENTA,
  PLAN_PRESUPUESTAL.NOMBRE,
  SUM(
  CASE
    WHEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO=0
    THEN DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO-DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO
    ELSE DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO -DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO
  END) VALOR
FROM DETALLE_COMPROBANTE_PPTAL
INNER JOIN PLAN_PRESUPUESTAL
ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
AND DETALLE_COMPROBANTE_PPTAL.CUENTA  = PLAN_PRESUPUESTAL.CODIGO
AND DETALLE_COMPROBANTE_PPTAL.ANO     = PLAN_PRESUPUESTAL.ANO
INNER JOIN DETALLE_COMPROBANTE_PPTAL DC_PPTAL
ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = DC_PPTAL.COMPANIA
AND DETALLE_COMPROBANTE_PPTAL.ANO_AFECT = DC_PPTAL.ANO
AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT = DC_PPTAL.TIPO_CPTE
AND DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO = DC_PPTAL.COMPROBANTE											 														 													  																												  
GROUP BY DETALLE_COMPROBANTE_PPTAL.COMPANIA,
  DETALLE_COMPROBANTE_PPTAL.ANO,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
  DC_PPTAL.CMPTE_AFECTADO,
  DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO,
  DETALLE_COMPROBANTE_PPTAL.CUENTA,
  PLAN_PRESUPUESTAL.NOMBRE
HAVING DETALLE_COMPROBANTE_PPTAL.ANO       = s$ano$s
AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE    = 's$tipoCpte$s'
AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE  = $P{PR_NUMCOMP}