SELECT LISAUXILIARES.COMPANIA NOCOMP,
       LISAUXILIARES.CUENTANOMBRE NOMBRECODIGO,
       LISAUXILIARES.CODIGO_CUENTA CODIGO,
       LISAUXILIARES.CENTRO_COSTO CODCENTROCOSTO,
       LISAUXILIARES.TERCERO_NOM NOMBRETERCERO,
       LISAUXILIARES.AUXILIAR CODAUXILIAR,
       LISAUXILIARES.AUXILIAR_NOM NOMBREAUXILIAR,
       TO_CHAR(LISAUXILIARES.FECHA,'DD/MM/YYYY') FECHA,
       LISAUXILIARES. TIPO_CPTE,  
       LISAUXILIARES.COMPROBANTE,
       LISAUXILIARES.SALDO_ANT SALDOINICIAL, 
       LISAUXILIARES.CONSECUTIVO,
       LISAUXILIARES.CENTRO_COSTO_NOM NOMBRECENTROCOSTO,
       CASE WHEN LISAUXILIARES.NRO_DOCUMENTO_DET IS NULL 
            THEN LISAUXILIARES.NRO_DOCUMENTO
            ELSE LISAUXILIARES.NRO_DOCUMENTO_DET
            END AS NODOCUMENTO, 
       CASE WHEN LISAUXILIARES.DESCRIPCION_DET IS NULL 
            THEN LISAUXILIARES.DESCRIPCION
            ELSE LISAUXILIARES.DESCRIPCION_DET
            END AS DESCRIP,
       LISAUXILIARES.VALOR_DEBITO VD,
       LISAUXILIARES.VALOR_CREDITO VC,
       LISAUXILIARES.SALDO 
FROM (
s$baseAuxiliar$s
)   LISAUXILIARES