SELECT V_PLAN_CONTABLE.ID,
  V_PLAN_CONTABLE.NOMBRE,
  CASE
    WHEN s$miles$s = 1
    THEN PCK_SYSMAN_UTL.FC_ROUND(V_PLAN_CONTABLE.NETOs$mes$s,-3)/1000
    ELSE V_PLAN_CONTABLE.NETOs$mes$s
  END AS ACUMULADOTOTAL,
  s$ultimoDia$s DIASMES,
  CASE
    WHEN s$miles$s = 1
    THEN PCK_SYSMAN_UTL.FC_ROUND((V_PLAN_CONTABLE.NETOs$mes$s/s$ultimoDia$s)-3)/1000
    ELSE V_PLAN_CONTABLE.NETOs$mes$s                   /s$ultimoDia$s
  END AS SALDOPROMEDIO,
   CASE
    WHEN s$miles$s = 1
    THEN PCK_SYSMAN_UTL.FC_ROUND((V_PLAN_CONTABLE.SALDOs$mes$s)-3)/1000
    ELSE V_PLAN_CONTABLE.SALDOs$mes$s 
  END AS SALDOFINDEMESMILES
  
FROM V_PLAN_CONTABLE
WHERE V_PLAN_CONTABLE.COMPANIA      = s$compania$s
AND V_PLAN_CONTABLE.ANO             = s$ano$s
AND V_PLAN_CONTABLE.CLASECUENTA     ='B'
AND V_PLAN_CONTABLE.MOVIMIENTO NOT IN(0)
