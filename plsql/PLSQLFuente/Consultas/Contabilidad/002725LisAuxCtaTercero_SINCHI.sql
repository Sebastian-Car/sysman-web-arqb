MERGE INTO CONSULTAS FIN USING (SELECT '002725LisAuxCtaTercero_SINCHI' INFORME ,TO_CLOB(q'[SELECT V_DETALLE_AUXILIAR_CNT.COMPANIA,
       V_DETALLE_AUXILIAR_CNT.ANO, 
       V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA, 
       V_DETALLE_AUXILIAR_CNT.TERCERO_DET AS TERCERO,  
       CASE WHEN TERCERO.NATURALEZA = 'N' THEN
            'Natural'
        ELSE
            'Juridica'
        END AS TIPO_P,
       V_DETALLE_AUXILIAR_CNT.SUCURSAL_DET SUCURSAL,
       V_DETALLE_AUXILIAR_CNT.DIGITOVERIFICACION,
       V_DETALLE_AUXILIAR_CNT.TERCERO_DET_NOM  TERCERONOMBRE,
       V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE,  
       s$groupBySinTotal$s
       SUM(V_DETALLE_AUXILIAR_CNT.VALOR_DEBITO)  AS SUMADEBITO,
       SUM(V_DETALLE_AUXILIAR_CNT.VALOR_CREDITO) AS SUMACREDITO,
       SUM(V_DETALLE_AUXILIAR_CNT.BASE_GRAVABLE) AS SUMABASE,
       V_DETALLE_AUXILIAR_CNT.NATURALEZA,
       CASE WHEN V_DETALLE_AUXILIAR_CNT.NATURALEZA= 'D' THEN 
        SUM(V_DETALLE_AUXILIAR_CNT.VALOR_DEBITO) - SUM(V_DETALLE_AUXILIAR_CNT.VALOR_CREDITO)
       ELSE
        SUM(V_DETALLE_AUXILIAR_CNT.VALOR_CREDITO) - SUM(V_DETALLE_AUXILIAR_CNT.VALOR_DEBITO)
       END AS SALDOTERCERO  
FROM V_DETALLE_AUXILIAR_CNT 
LEFT JOIN TERCERO ON TERCERO.COMPANIA = V_DETALLE_AUXILIAR_CNT.COMPANIA
                  AND TERCERO.NIT = V_DETALLE_AUXILIAR_CNT.TERCERO_DET
                  AND TERCERO.SUCURSAL = V_DETALLE_AUXILIAR_CNT.SUCURSAL_DET
WHERE V_DETALLE_AUXILIAR_CNT.COMPANIA = s$compania$s 
  AND V_DETALLE_AUXILIAR_CNT.ANO      = s$anoTrabajo$s
  AND V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA   BETWEEN 's$codigoInicial$s'
                                          AND 's$codigoFinal$s'
  AND V_DETALLE_AUXILIAR_CNT.FECHA BETWEEN s$fechaInicial$s
                                       AND s$fechaFinal$s
  AND TRIM(V_DETALLE_AUXILIAR_CNT.TERCERO_NOM)  BETWEEN 's$terceroInicial$s'
                                               AND 's$terceroFinal$s'
  AND V_DETALLE_AUXILIAR_CNT.TIPO_CPTE BETWEEN 's$tipoInicial$s'
                                           AND 's$tipoFinal$s'
GROUP BY V_DETALLE_AUXILIAR_CNT.COMPANIA,
       V_DETALLE_AUXILIAR_CNT.ANO, 
       V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA, 
       V_DETALLE_AUXILIAR_CNT.TERCERO_DET,
       TERCERO.NATURALEZA, 
       V_DETALLE_AUXILIAR_CNT.SUCURSAL_DET,
       V_DETALLE_AUXILIAR_CNT.DIGITOVERIFICACION,
       V_DETALLE_AUXILIAR_CNT.TERCERO_DET_NOM,
       V_DETALLE_AUXILIAR_CNT.NATURALEZA, 
       s$groupBySinTotal$s 
       V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE 
s$havingSinDebito$s]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, 'MPEREZ' CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);