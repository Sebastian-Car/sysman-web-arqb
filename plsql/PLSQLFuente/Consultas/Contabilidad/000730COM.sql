SELECT V_DETALLE_AUXILIAR_CNT.COMPANIA ,
       V_DETALLE_AUXILIAR_CNT.ANO ,
       V_DETALLE_AUXILIAR_CNT.TIPO_CPTE TIPO ,
       V_DETALLE_AUXILIAR_CNT.TIPO_COMPROBANTE_NOM ,
       V_DETALLE_AUXILIAR_CNT.COMPROBANTE ,
       V_DETALLE_AUXILIAR_CNT.FECHA ,
       V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO_COM CENTRO_COSTO ,
       V_DETALLE_AUXILIAR_CNT.TERCERO_COM TERCERO ,
       TRIM(V_DETALLE_AUXILIAR_CNT.TERCERO_COM_NOM) TERCERO_NOM ,
       V_DETALLE_AUXILIAR_CNT.TERCERO_COM_DIR DIRECCION ,
       V_DETALLE_AUXILIAR_CNT.TERCERO_COM_TEL TELEFONOS ,
       CASE
           WHEN V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO_DET NOT IN(PCK_DATOS.FC_CONS_CENTRO) THEN ' / '||V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO_DET
       END CODCENTRO ,
       V_DETALLE_AUXILIAR_CNT.NRO_DOCUMENTO ,
       V_DETALLE_AUXILIAR_CNT.DESCRIPCION ,
       V_DETALLE_AUXILIAR_CNT.TEXTO ,
       V_DETALLE_AUXILIAR_CNT.VLRAGIRAR ,
       V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA ,
       CASE
           WHEN V_DETALLE_AUXILIAR_CNT.VALOR_DEBITO NOT IN(0) THEN 'A' || V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA
           ELSE 'Z' || V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA
       END SEC ,
       TRIM(V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE) || CASE
                                                        WHEN V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO_DET NOT IN(PCK_DATOS.FC_CONS_CENTRO) THEN ' / '||V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO_DET_NOM
                                                    END || CASE
                                                               WHEN V_DETALLE_AUXILIAR_CNT.TERCERO_DET NOT IN(PCK_DATOS.FC_CONS_TERCERO) THEN ' / '||V_DETALLE_AUXILIAR_CNT.TERCERO_DET_NOM
                                                           END || CASE
                                                                      WHEN V_DETALLE_AUXILIAR_CNT.AUXILIAR_DET NOT IN(PCK_DATOS.FC_CONS_AUXILIAR) THEN ' / '||V_DETALLE_AUXILIAR_CNT.AUXILIAR_DET_NOM
                                                                  END || CASE
                                                                             WHEN V_DETALLE_AUXILIAR_CNT.REFERENCIA_DET NOT IN(PCK_DATOS.FC_CONS_REFERENCIA) THEN ' / '||V_DETALLE_AUXILIAR_CNT.REFERENCIA_DET_NOM
                                                                         END TOTALNOMBRE ,
                                                                         V_DETALLE_AUXILIAR_CNT.VALOR_DEBITO ,
                                                                         V_DETALLE_AUXILIAR_CNT.VALOR_CREDITO
FROM V_DETALLE_AUXILIAR_CNT
WHERE V_DETALLE_AUXILIAR_CNT.COMPANIA = 's$compania$s'
  AND V_DETALLE_AUXILIAR_CNT.ANO = s$ano$s
  AND V_DETALLE_AUXILIAR_CNT.TIPO_CPTE = 's$tipoCpte$s'
  AND V_DETALLE_AUXILIAR_CNT.COMPROBANTE BETWEEN 's$numeroPptoInicial$s' AND 's$numeroPptoFinal$s'
