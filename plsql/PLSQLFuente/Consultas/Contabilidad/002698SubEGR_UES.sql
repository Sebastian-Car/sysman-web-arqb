MERGE INTO CONSULTAS FIN USING (SELECT '002698SubEGR_UES' INFORME ,TO_CLOB(q'[SELECT  DC.COMPANIA AS COMPANIA, 
        DC.ANO AS ANO, 
        DC.TIPO_CPTE AS TIPO, 
        DC.COMPROBANTE AS COMPROBANTE, 
        MIN(V_PLAN_PRESUPUESTAL.NOMBRE) KEEP(DENSE_RANK FIRST ORDER BY ROWNUM) AS PRIMERODENOMBRE, 
        V_PLAN_PRESUPUESTAL.ID AS CUENTA, 
        DC2.COMPROBANTE AS NUMERORES, 
        DC2.CMPTE_AFECTADO AS NUMERODIS, 
        SUM(DC.VALOR_DEBITO-DC.VALOR_CREDITO) AS VALORNETO, 
        DC2.TIPO_CPTE AS TIPO_CPTE2, 
        DC2.COMPROBANTE AS NUMERORES1, 
        DC2.TIPO_CPTE_AFECT AS TIPO_CPTE_AFECT2, 
        DC2.CMPTE_AFECTADO AS NUMERODIS1, 
        DC1.TIPO_CPTE AS TIPO_CPTE1, 
        DC1.COMPROBANTE AS NORES, 
        DC1.TIPO_CPTE_AFECT AS TIPO_CPTE_AFECT1, 
        DC1.CMPTE_AFECTADO AS NODIS 
FROM ((DETALLE_COMPROBANTE_PPTAL DC LEFT JOIN DETALLE_COMPROBANTE_PPTAL DC1 ON  
        DC.COMPANIA             = DC1.COMPANIA  
        AND DC.ANO              = DC1.ANO 
        AND DC.TIPO_CPTE_AFECT  = DC1.TIPO_CPTE 
        AND DC.CMPTE_AFECTADO   = DC1.COMPROBANTE 
        AND DC.CONSECUTIVOPPTO  = DC1.CONSECUTIVO)
 LEFT JOIN DETALLE_COMPROBANTE_PPTAL DC2 ON  
    DC1.COMPANIA            = DC2.COMPANIA 
    AND DC1.ANO             = DC2.ANO  
    AND DC1.TIPO_CPTE_AFECT = DC2.TIPO_CPTE  
    AND DC1.CMPTE_AFECTADO  = DC2.COMPROBANTE  
    AND DC1.CONSECUTIVOPPTO = DC2.CONSECUTIVO) 
LEFT JOIN V_PLAN_PRESUPUESTAL 
 ON DC.COMPANIA    = V_PLAN_PRESUPUESTAL.COMPANIA
    AND DC.ANO        = V_PLAN_PRESUPUESTAL.ANO
    AND DC.CUENTA        = V_PLAN_PRESUPUESTAL.CODIGO
    AND DC.CENTRO_COSTOI = V_PLAN_PRESUPUESTAL.CENTRO_COSTO
    AND DC.AUXILIARI      = V_PLAN_PRESUPUESTAL.AUXILIAR
    AND DC.REFERENCIAI  = V_PLAN_PRESUPUESTAL.REFERENCIA
    AND DC.FUENTE_RECURSOI         = V_PLAN_PRESUPUESTAL.FUENTE_RECURSO
WHERE DC.COMPANIA = $P{PR_COMPANIA} 
    AND DC.ANO         = $P{PR_ANO} 
   AND DC.TIPO_CPTE= $P{PR_TIPO} 
    AND DC.COMPROBANTE BETWEEN $P{PR_COMPROBANTE} AND $P{PR_COMPROBANTE} 
GROUP BY DC.COMPANIA, 
    DC.ANO, 
    DC.TIPO_CPTE, 
    DC.COMPROBANTE, 
    V_PLAN_PRESUPUESTAL.ID, 
    DC2.TIPO_CPTE, 
    DC2.COMPROBANTE, 
    DC2.TIPO_CPTE_AFECT, 
    DC2.CMPTE_AFECTADO, 
    DC1.TIPO_CPTE, 
    DC1.COMPROBANTE, 
    DC1.TIPO_CPTE_AFECT, 
    DC1.CMPTE_AFECTADO, 
    DC.TIPO_CPTE, 
    DC2.COMPROBANTE, 
    DC2.CMPTE_AFECTADO]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
    
MERGE INTO CONSULTAS_SUB FIN USING (SELECT '002697EGR_UES' INFORME_PADRE, '002698SubEGR_UES' INFORME_HIJO, 'PR_STRSQL_INFORMESUBCOM_DCEGR' PARAMETRO ,NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (FIN.INFORME_PADRE = INI.INFORME_PADRE AND INI.INFORME_HIJO = FIN.INFORME_HIJO)  WHEN MATCHED THEN  UPDATE SET FIN.PARAMETRO = INI.PARAMETRO , FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME_PADRE,INFORME_HIJO, PARAMETRO,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME_PADRE,INI.INFORME_HIJO, INI.PARAMETRO,INI.CREATED_BY,SYSDATE);
