SELECT
     DETALLE_COMPROBANTE_CNT.COMPANIA,
    DETALLE_COMPROBANTE_CNT.ANO ANOCOMP,
    DETALLE_COMPROBANTE_CNT.COMPROBANTE NUMERO,
    DETALLE_COMPROBANTE_CNT.TIPO_CPTE TIPO,
    TIPO_COMPROBANTE.NOMBRE AS NOMBRETIPOCNT,
    TERCERO.NOMBRE   NOMTERCERO,
    TIPO_COMPROBANTE.NOMBRE NOMBRETIPOCOMPROBANTE,
    TO_CHAR(COMPROBANTE_CNT.FECHA,'DD/MM/YYYY')FECHA,
    TERCERO.NOMBRE NOMBRETERCERO1,
    TERCERO.DIRECCION TERCERODIRE,
    COMPROBANTE_CNT.TERCERO,
     CASE WHEN 
      CASE WHEN DETALLE_COMPROBANTE_CNT.CENTRO_COSTO = 's$centroCosto$s' 
        THEN '' 
        ELSE DETALLE_COMPROBANTE_CNT.CENTRO_COSTO 
      END = ''
      THEN '' 
      ELSE 's$centroCosto$s' 
    END || ' / ' || CENTRO_COSTO.NOMBRE CENTRO, 
    COMPROBANTE_CNT.TERCERO || ' - ' || TERCERO.DIGITOVERIFICACION AS NITTERCEROC,
    CASE
    WHEN TERCERO.CODIGOPOSTAL IS NULL
    OR TERCERO.CODIGOPOSTAL    = ''
    THEN PCK_SYSMAN_UTL.FC_PAR(DETALLE_COMPROBANTE_CNT.COMPANIA,'CODIGO POSTAL',1,SYSDATE,-1)
    ELSE TERCERO.CODIGOPOSTAL
  END AS CODIGOPOSTAL,
    DECODE (TERCERO.TIPOID, 'N','NIT','C','Cédula de ciudadanía','E','Cédula de extranjería','T','Tarjeta de entidad',
            'R','Cédula de ciudadanía del tutor o representante') TIPOID,
    TERCERO.TIPOID TIPOD,
    PLAN_CONTABLE.NOMBRE || CASE WHEN NVL(TERCERO.NIT,'')= '' THEN '' ELSE '/' END ||
      NVL(TERCERO.NOMBRE,'') || CASE WHEN NVL(AUXILIAR.CODIGO,'')= '' THEN '' ELSE '/' END || 
      NVL(AUXILIAR.NOMBRE,'') AS TOTALNOMBRE, 
    COMPROBANTE_CNT.TERCERO,
    TERCERO.TELEFONOS,
    TERCERO.DIRECCION,
    COMPROBANTE_CNT.DESCRIPCION,
    COMPROBANTE_CNT.TEXTO,
    COMPROBANTE_CNT.NRO_DOCUMENTO,
    TO_CHAR(COMPROBANTE_CNT.FECHA_VCN_DOC,'DD/MM/YYYY')FECHA_VCN_DOC,
    COMPROBANTE_CNT.VLR_DOCUMENTO,
    DETALLE_COMPROBANTE_CNT.CUENTA CODIGO_CUENTA,
    DETALLE_COMPROBANTE_CNT.AUXILIAR,
    DETALLE_COMPROBANTE_CNT.REFERENCIA,
    CASE WHEN DETALLE_COMPROBANTE_CNT.VALOR_DEBITO NOT IN(0) 
    THEN 'a' || DETALLE_COMPROBANTE_CNT.CUENTA
    ELSE 'z' || DETALLE_COMPROBANTE_CNT.CUENTA
    END SEC,
    DETALLE_COMPROBANTE_CNT.VALOR_DEBITO,
    DETALLE_COMPROBANTE_CNT.VALOR_CREDITO,
    NVL(COMPROBANTE_CNT.VLRAGIRAR,0) AS VLRAGIRAR,
    TIPOORDENDECOMPRA.NOMBRE NOMBRETIPOORDEN,
    COMPROBANTE_CNT.NUMEROCONTRATO || ' / ' || ORDENDECOMPRA.CODEQUIVALENTE AS NOCONTRATO,
    COMPROBANTE_CNT.MODIFIED_BY MODIFICADOR,
    TIPOORDENDECOMPRA.NOMBRE TIPOCONTRATOS,
    COMPROBANTE_CNT.NUMEROCONTRATO NOCONTRATO,
    COMPROBANTE_CNT.CREATED_BY CREADOR,
    NVL(BANCO.NOMBREBANCO,' ')NOMBREBANCO
FROM DETALLE_COMPROBANTE_CNT
INNER JOIN COMPROBANTE_CNT
    ON COMPROBANTE_CNT.COMPANIA          = DETALLE_COMPROBANTE_CNT.COMPANIA
    AND COMPROBANTE_CNT.ANO              = DETALLE_COMPROBANTE_CNT.ANO
    AND COMPROBANTE_CNT.TIPO             = DETALLE_COMPROBANTE_CNT.TIPO_CPTE
    AND COMPROBANTE_CNT.NUMERO           = DETALLE_COMPROBANTE_CNT.COMPROBANTE
  INNER JOIN PLAN_CONTABLE
    ON DETALLE_COMPROBANTE_CNT.COMPANIA     = PLAN_CONTABLE.COMPANIA 
    AND DETALLE_COMPROBANTE_CNT.ANO         = PLAN_CONTABLE.ANO
    AND DETALLE_COMPROBANTE_CNT.CUENTA      = PLAN_CONTABLE.CODIGO
  INNER JOIN TERCERO
    ON DETALLE_COMPROBANTE_CNT.COMPANIA  = TERCERO.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL
    AND DETALLE_COMPROBANTE_CNT.TERCERO  = TERCERO.NIT  
  INNER JOIN AUXILIAR
    ON DETALLE_COMPROBANTE_CNT.COMPANIA   = AUXILIAR.COMPANIA 
    AND DETALLE_COMPROBANTE_CNT.AUXILIAR  = AUXILIAR.CODIGO  
    AND DETALLE_COMPROBANTE_CNT.ANO       = AUXILIAR.ANO
  INNER JOIN CENTRO_COSTO
    ON DETALLE_COMPROBANTE_CNT.COMPANIA       = CENTRO_COSTO.COMPANIA 
    AND DETALLE_COMPROBANTE_CNT.CENTRO_COSTO  = CENTRO_COSTO.CODIGO 
    AND DETALLE_COMPROBANTE_CNT.ANO           = CENTRO_COSTO.ANO 
  INNER JOIN TIPO_COMPROBANTE
    ON DETALLE_COMPROBANTE_CNT.COMPANIA   = TIPO_COMPROBANTE.COMPANIA 
    AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
  LEFT JOIN TIPOORDENDECOMPRA
    ON COMPROBANTE_CNT.COMPANIA     = TIPOORDENDECOMPRA.COMPANIA 
    AND COMPROBANTE_CNT.TIPOCONTRATO = TIPOORDENDECOMPRA.CODIGO
  LEFT JOIN ORDENDECOMPRA
    ON COMPROBANTE_CNT.COMPANIA        = ORDENDECOMPRA.COMPANIA
    AND COMPROBANTE_CNT.TIPOCONTRATO   = ORDENDECOMPRA.CLASEORDEN
    AND COMPROBANTE_CNT.NUMEROCONTRATO = ORDENDECOMPRA.NUMERO
LEFT JOIN BANCO 
  ON COMPROBANTE_CNT.COMPANIA = BANCO.COMPANIA
  AND COMPROBANTE_CNT.BANCO = BANCO.BANCO
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA    = s$compania$s
  AND DETALLE_COMPROBANTE_CNT.ANO         = s$ano$s
  AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE   ='s$tipoCpte$s' 
  AND DETALLE_COMPROBANTE_CNT.COMPROBANTE BETWEEN  's$numeroPptoInicial$s' AND 's$numeroPptoFinal$s'
