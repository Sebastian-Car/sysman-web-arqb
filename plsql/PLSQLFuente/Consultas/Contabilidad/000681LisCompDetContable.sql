SELECT  V_DETALLE_AUXILIAR_CNT.COMPANIA,
          V_DETALLE_AUXILIAR_CNT.TIPO_CPTE,
          V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA, 
          V_DETALLE_AUXILIAR_CNT.NRO_DOCUMENTO, 
          V_DETALLE_AUXILIAR_CNT.VALOR_DEBITO, 
          V_DETALLE_AUXILIAR_CNT.VALOR_CREDITO, 
          V_DETALLE_AUXILIAR_CNT.TERCERO_NOM NOMBRETERCERO,  
          V_DETALLE_AUXILIAR_CNT.COMPROBANTE, 
          V_DETALLE_AUXILIAR_CNT.FECHA, 
          V_DETALLE_AUXILIAR_CNT.DESCRIPCION, 
          V_DETALLE_AUXILIAR_CNT.TIPO_COMPROBANTE_NOM,
          V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE, 
          V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO_NOM NOMBRECENTRO 
  FROM    V_DETALLE_AUXILIAR_CNT
  WHERE   V_DETALLE_AUXILIAR_CNT.COMPANIA   = s$compania$s
  AND     TO_DATE(TO_CHAR(V_DETALLE_AUXILIAR_CNT.FECHA,'DD/MM/YYYY'))  BETWEEN  TO_DATE( 's$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
  AND     V_DETALLE_AUXILIAR_CNT.TIPO_CPTE  BETWEEN s$tipoInicial$s AND s$tipoFinal$s
  ORDER BY V_DETALLE_AUXILIAR_CNT.FECHA,
                    V_DETALLE_AUXILIAR_CNT.COMPROBANTE, 
                    V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA
