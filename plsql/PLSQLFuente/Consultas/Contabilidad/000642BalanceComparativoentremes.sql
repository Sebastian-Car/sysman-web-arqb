SELECT PLAN_CONTABLE.CODIGO, 
 PLAN_CONTABLE.CENTRO_COSTO, 
 PLAN_CONTABLE.AUXILIAR, 
 PLAN_CONTABLE.TERCERO, 
 PLAN_CONTABLE.SUCURSAL, 
 PLAN_CONTABLE.NOMBRE, 
SUBSTR(PLAN_CONTABLE.CODIGO,1,1) CLASE,
 PLAN_CONTABLE.CODIGO || PLAN_CONTABLE.CENTRO_COSTO ||  PLAN_CONTABLE.NOMBRE AS ORDENCUENTAS,
 PLAN_CONTABLE.COMPANIA, 
 PLAN_CONTABLE.SALDOs$mesTrabajo$s   AS SANTERIOR, 
 PLAN_CONTABLE.NETOs$mesTrabajo$s   AS NANTERIOR, 
 PLAN_CONTABLE_1.SALDOs$mesComparar$s   AS NUEVO, 
 PLAN_CONTABLE_1.NETOs$mesComparar$s   AS NNUEVO, 
 CASE WHEN SUBSTR(PLAN_CONTABLE.CODIGO,1,1)='0' THEN 'z'||PLAN_CONTABLE.CODIGO ELSE PLAN_CONTABLE.CODIGO END ORDEN, 
 SUBSTR(CASE WHEN SUBSTR(PLAN_CONTABLE.CODIGO,1,1)='0' THEN 'z'||PLAN_CONTABLE.CODIGO ELSE PLAN_CONTABLE.CODIGO END,1,1) AS CLASEORDEN 
 FROM V_PLAN_CONTABLE PLAN_CONTABLE 
      INNER JOIN V_PLAN_CONTABLE PLAN_CONTABLE_1 
            ON PLAN_CONTABLE.COMPANIA = PLAN_CONTABLE_1.COMPANIA
            AND PLAN_CONTABLE.CODIGO = PLAN_CONTABLE_1.CODIGO 
WHERE PLAN_CONTABLE.COMPANIA=s$compania$s
      AND PLAN_CONTABLE.ANO=s$anioTrabajo$s
      and PLAN_CONTABLE_1.ANO =s$anioComparar$s
      AND PLAN_CONTABLE.CODIGO BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
      AND LENGTH(PLAN_CONTABLE.CODIGO) <=  s$digitos$s
      s$auxiliarGeneral$s
      s$centroCosto$s
      s$tercero$s
      s$saldoCero$s
