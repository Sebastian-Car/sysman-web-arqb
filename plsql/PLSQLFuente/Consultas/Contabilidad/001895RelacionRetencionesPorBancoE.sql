SELECT DETALLE_COMPROBANTE_CNT.CUENTA CODIGO_CUENTA,
  COMPROBANTE_CNT.TIPO,
  COMPROBANTE_CNT.NUMERO,
  COMPROBANTE_CNT.DESCRIPCION,
  COMPROBANTE_CNT.FECHA,
  TERCERO.NOMBRE NOMBRET,
  PLAN_CONTABLE.NOMBRE NOMBREPLC,
  PLAN_CONTABLE.CODIGO,
  SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO) SUMADEVALOR_DEBITO,
  SUM(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO) SUMADEVALOR_CREDITO,
  CASE WHEN DETALLE_COMPROBANTE_CNT.CUENTA <> DETALLE_COMPROBANTE_CNT.CUENTA THEN PLAN_CONTABLE_1.NOMBRE  ELSE PLAN_CONTABLE_1.NOMBRE  END TOTALNOMBRE,
  NVL(COMPROBANTE_CNT.CUENTA,'') CTABANCO,
  CASE WHEN TRIM(PLAN_CONTABLE.NOMBRE) IS NOT NULL THEN PLAN_CONTABLE.NOMBRE ELSE 'SIN BANCO' END NOMBREBANCO,
  COMPROBANTE_CNT.COMPANIA,
  COMPROBANTE_CNT.ANO,
  NVL(COMPROBANTE_CNT.CUENTA,'') CUENTAB,
  DETALLE_COMPROBANTE_CNT.CUENTA CUENTADTCP,
  DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE,
  CASE WHEN COMPROBANTE_CNT.TIPO IN ('COM') THEN TO_CHAR(COMPROBANTE_CNT_1.NUMERO) ELSE '' END EGR
FROM DETALLE_COMPROBANTE_CNT
 LEFT JOIN PLAN_CONTABLE PLAN_CONTABLE_1
 ON DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE_1.COMPANIA
 AND DETALLE_COMPROBANTE_CNT.ANO     = PLAN_CONTABLE_1.ANO
 AND DETALLE_COMPROBANTE_CNT.CUENTA  = PLAN_CONTABLE_1.CODIGO
 LEFT JOIN PLAN_CONTABLE PLAN_CONTABLE_2
 ON DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE_2.COMPANIA
 AND DETALLE_COMPROBANTE_CNT.ANO     = PLAN_CONTABLE_2.ANO
 AND DETALLE_COMPROBANTE_CNT.CUENTA  = PLAN_CONTABLE_2.CODIGO
 LEFT JOIN COMPROBANTE_CNT
 LEFT JOIN PLAN_CONTABLE
 ON COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA
 AND COMPROBANTE_CNT.ANO     = PLAN_CONTABLE.ANO
 AND COMPROBANTE_CNT.CUENTA  = PLAN_CONTABLE.CODIGO
 LEFT JOIN TERCERO
 ON COMPROBANTE_CNT.COMPANIA  = TERCERO.COMPANIA
 AND COMPROBANTE_CNT.TERCERO  = TERCERO.NIT
 AND COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL
 LEFT JOIN COMPROBANTE_CNT COMPROBANTE_CNT_1
 ON COMPROBANTE_CNT.COMPANIA = COMPROBANTE_CNT_1.COMPANIA
 AND COMPROBANTE_CNT.ANO     = COMPROBANTE_CNT_1.ANO
 AND COMPROBANTE_CNT.TIPO    = COMPROBANTE_CNT_1.TIPO
 AND COMPROBANTE_CNT.NUMERO  = COMPROBANTE_CNT_1.NUMERO
 LEFT JOIN TIPO_COMPROBANTE
 ON COMPROBANTE_CNT.COMPANIA             = TIPO_COMPROBANTE.COMPANIA
 AND COMPROBANTE_CNT.TIPO                = TIPO_COMPROBANTE.CODIGO
 ON DETALLE_COMPROBANTE_CNT.COMPANIA     = COMPROBANTE_CNT.COMPANIA
 AND DETALLE_COMPROBANTE_CNT.ANO         = COMPROBANTE_CNT.ANO
 AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE   = COMPROBANTE_CNT.TIPO
 AND DETALLE_COMPROBANTE_CNT.COMPROBANTE = COMPROBANTE_CNT.NUMERO
WHERE COMPROBANTE_CNT.COMPANIA          =  s$compania$s
 AND DETALLE_COMPROBANTE_CNT.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
 AND TIPO_COMPROBANTE.CLASE_CONTABLE NOT IN ('P')
 AND PLAN_CONTABLE_1.CLASECUENTA      IN ('I')
GROUP BY DETALLE_COMPROBANTE_CNT.CUENTA,
    COMPROBANTE_CNT.TIPO,
    COMPROBANTE_CNT.NUMERO,
    COMPROBANTE_CNT.DESCRIPCION,
    COMPROBANTE_CNT.FECHA,
    TERCERO.NOMBRE,
    PLAN_CONTABLE.NOMBRE,
    PLAN_CONTABLE.CODIGO,
    COMPROBANTE_CNT.COMPANIA,
    COMPROBANTE_CNT.ANO,
    NVL(COMPROBANTE_CNT.CUENTA,'') ,
    PLAN_CONTABLE_1.NOMBRE,
    PLAN_CONTABLE_2.NOMBRE,
    DETALLE_COMPROBANTE_CNT.CUENTA,
    DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE,
    CASE WHEN COMPROBANTE_CNT.TIPO IN ('COM') THEN TO_CHAR(COMPROBANTE_CNT_1.NUMERO)	ELSE ''  END
HAVING PLAN_CONTABLE.CODIGO BETWEEN s$bancoInicial$s AND s$bancoFinal$s
    OR NVL (COMPROBANTE_CNT.CUENTA,'') = '' 
ORDER BY PLAN_CONTABLE.CODIGO
