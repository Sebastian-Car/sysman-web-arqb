SELECT
	REGISTROTRASLADOS,
	NITTERCERO,
	REFERENCIA,
	CUENTA,
	TIPOCUENTA,
	BANCO,
	REPLACE(REPLACE(LPAD(TRUNC(VALORTRASLADO), 16, '0') || TO_CHAR((VALORTRASLADO-FLOOR(VALORTRASLADO)),'fm.00'), '.', ''), ' ', '') VALOR_TRASLADO,
	TALON,
	TIPO_ID,
	VALIDAR_TRAS_ACH,
	RES_PROCESO,
	MENSAJE_RESPUESTA,
	VALOR_ACUM_COBRO,
	FECHA_APLICACION,
	OFICINA_RECAUDO,
	MOTIVO,
	VAL
FROM
	(
	SELECT
		'TR' REGISTROTRASLADOS,
		LPAD(COMPROBANTE_CNT.TERCERO , 16, 0) NITTERCERO,
		'0000000000000000' REFERENCIA,
		LPAD(TERCEROPAGOS.CUENTA, 16, 0) CUENTA,
		CASE
			TERCEROPAGOS.TIPOCUENTA 
        WHEN 'A' THEN 'CA'
			WHEN 'C' THEN 'CC'
		END TIPOCUENTA,
		LPAD(TERCEROPAGOS.BANCO, 6, 0) BANCO,
		NVL((
	SELECT
		NVL(SUM(DT.VALOR_CREDITO), '0.00')
	FROM
		DETALLE_COMPROBANTE_CNT DT
	WHERE
		DT.CUENTA = DETALLE_COMPROBANTE_CNT.CUENTA
		AND DT.COMPANIA = COMPROBANTE_CNT.COMPANIA
		AND DT.ANO = COMPROBANTE_CNT.ANO
		AND DT.TIPO_CPTE = COMPROBANTE_CNT.TIPO
		AND DT.COMPROBANTE = COMPROBANTE_CNT.NUMERO ), '0.00') VALORTRASLADO,
		'000000' TALON,
		CASE
			TERCERO.TIPOID
		WHEN 'N' THEN '01'
			WHEN 'C' THEN '02'
		END TIPO_ID,
		'1' VALIDAR_TRAS_ACH,
		'9999' RES_PROCESO,
		'0000000000000000000000000000000000000000' MENSAJE_RESPUESTA,
		'000000000000000000' VALOR_ACUM_COBRO,
		'00000000' FECHA_APLICACION,
		'0000' OFICINA_RECAUDO,
		'0000' MOTIVO,
		'0000000' VAL
	FROM
		COMPROBANTE_CNT
	INNER JOIN DETALLE_COMPROBANTE_CNT
                          ON
		DETALLE_COMPROBANTE_CNT.COMPANIA = COMPROBANTE_CNT.COMPANIA
		AND DETALLE_COMPROBANTE_CNT.ANO = COMPROBANTE_CNT.ANO
		AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = COMPROBANTE_CNT.TIPO
		AND DETALLE_COMPROBANTE_CNT.COMPROBANTE = COMPROBANTE_CNT.NUMERO
	INNER JOIN TERCERO
                          ON
		DETALLE_COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA
		AND DETALLE_COMPROBANTE_CNT.TERCERO = TERCERO.NIT
		AND DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL
	LEFT JOIN TERCERO APODERADO
                          ON
		TERCERO.COMPANIA = APODERADO.COMPANIA
		AND TERCERO.NITAPODERADO = APODERADO.NIT
		AND TERCERO.SUCURSALAPODERADO = APODERADO.SUCURSAL
	LEFT JOIN TERCEROPAGOS
                          ON
		TERCEROPAGOS.COMPANIA = COMPROBANTE_CNT.COMPANIA
		AND TERCEROPAGOS.NIT = COMPROBANTE_CNT.TERCERO
		AND TERCEROPAGOS.SUCURSAL = COMPROBANTE_CNT.SUCURSAL
		AND TERCEROPAGOS.BANCO = COMPROBANTE_CNT.BANCO
		AND TERCEROPAGOS.CUENTA = COMPROBANTE_CNT.CUENTABANCO
	LEFT JOIN BANCO
                          ON
		TERCEROPAGOS.COMPANIA = BANCO.COMPANIA
		AND TERCEROPAGOS.BANCO = BANCO.BANCO
WHERE
	COMPROBANTE_CNT.COMPANIA = s$compania$s
	AND COMPROBANTE_CNT.ANO = s$ano$s
	AND COMPROBANTE_CNT.TIPO = 's$tipo$s'
	AND DETALLE_COMPROBANTE_CNT.CUENTA BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
	AND COMPROBANTE_CNT.NUMERO BETWEEN s$numeroInicial$s AND s$numeroFinal$s
	AND COMPROBANTE_CNT.PAGOENPLANO IN (-1)
	AND COMPROBANTE_CNT.ENVIADO IN (0)
	AND NVL(TERCEROPAGOS.BANCO, '0') NOT IN ('0'))