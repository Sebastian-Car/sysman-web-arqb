SELECT 
	V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA,
	V_DETALLE_AUXILIAR_CNT.TIPO_CPTE,
	V_DETALLE_AUXILIAR_CNT.COMPROBANTE,
	V_DETALLE_AUXILIAR_CNT.DESCRIPCION,
	V_DETALLE_AUXILIAR_CNT.FECHA,
	TERCERO.NOMBRE,
	V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE,
	SUM(V_DETALLE_AUXILIAR_CNT.VALOR_DEBITO) SUMADEVALOR_DEBITO,
	SUM(V_DETALLE_AUXILIAR_CNT.VALOR_CREDITO) SUMADEVALOR_CREDITO,
	CASE WHEN V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA_DET <> V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA 
	     THEN V_DETALLE_AUXILIAR_CNT.TERCERO_NOM 
	     ELSE V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE 
	END TOTALNOMBRE,
	NVL(V_DETALLE_AUXILIAR_CNT.CUENTA_COMP,'') CTABANCO,
	CASE WHEN TRIM(V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE) IS NOT NULL
	     THEN V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE 
	     ELSE 'Sin Banco' 
	END NOMBREBANCO,
	V_DETALLE_AUXILIAR_CNT.COMPANIA,
	V_DETALLE_AUXILIAR_CNT.ANO,
	NVL(V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA_DET,' ') CUENTAB,
	V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE,
	V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA_DET,
	V_DETALLE_AUXILIAR_CNT.BASE_GRAVABLE
FROM V_DETALLE_AUXILIAR_CNT
	LEFT JOIN TERCERO
		ON V_DETALLE_AUXILIAR_CNT.COMPANIA  = TERCERO.COMPANIA
		AND V_DETALLE_AUXILIAR_CNT.TERCERO  = TERCERO.NIT
		AND V_DETALLE_AUXILIAR_CNT.SUCURSAL = TERCERO.SUCURSAL
WHERE   V_DETALLE_AUXILIAR_CNT.COMPANIA = s$compania$s
		AND V_DETALLE_AUXILIAR_CNT.FECHA BETWEEN  TO_DATE(s$fechaInicial$s,'DD/MM/YYYY') AND TO_DATE(s$fechaFinal$s,'DD/MM/YYYY')
		AND NVL (V_DETALLE_AUXILIAR_CNT.CUENTA_COMP,'') BETWEEN s$bancoInicial$s AND s$bancoFinal$s
		AND V_DETALLE_AUXILIAR_CNT.CLASE_CONTABLE NOT IN ('P') 
		AND V_DETALLE_AUXILIAR_CNT.CLASECUENTA IN ('I')
GROUP BY 
	V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA,
	V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA_DET,
	V_DETALLE_AUXILIAR_CNT.TIPO_CPTE,
	V_DETALLE_AUXILIAR_CNT.COMPROBANTE,
	V_DETALLE_AUXILIAR_CNT.DESCRIPCION,
	V_DETALLE_AUXILIAR_CNT.FECHA,
	TERCERO.NOMBRE,
	V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE,
	V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE,
	CASE WHEN V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA_DET<>V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA THEN V_DETALLE_AUXILIAR_CNT.TERCERO_NOM ELSE V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE END,
	V_DETALLE_AUXILIAR_CNT.COMPANIA,
	V_DETALLE_AUXILIAR_CNT.ANO,
	NVL(V_DETALLE_AUXILIAR_CNT.CUENTA_COMP,' '),
  	NVL(V_DETALLE_AUXILIAR_CNT.CUENTA_COMP,''),
	V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA_DET,
	V_DETALLE_AUXILIAR_CNT.BASE_GRAVABLE
