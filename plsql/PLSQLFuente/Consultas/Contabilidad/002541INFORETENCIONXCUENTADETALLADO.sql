MERGE INTO CONSULTAS FIN USING (SELECT '002541INFORETENCIONXCUENTADETALLADO' INFORME ,TO_CLOB(q'[SELECT  DETALLE_COMPROBANTE_CNT.COMPANIA, 
        DETALLE_COMPROBANTE_CNT.FECHA, 
        DETALLE_COMPROBANTE_CNT.COMPROBANTE, 
        TERCERO.NOMBRE AS NOMBRE_TERCERO, 
        RETENCIONES.NOMBRE AS CONCEPTO,  
        DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE,  
        SUM(COMPROBANTE_CNTRETENCION.VALOR) AS VR_RETENIDO, 
        RETENCIONES.PCT_APLICAR AS TARIFA
FROM DETALLE_COMPROBANTE_CNT
INNER JOIN TERCERO 
        ON DETALLE_COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA 
       AND DETALLE_COMPROBANTE_CNT.TERCERO = TERCERO.NIT 
       AND DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL 
INNER JOIN COMPROBANTE_CNTRETENCION 
        ON DETALLE_COMPROBANTE_CNT.COMPANIA = COMPROBANTE_CNTRETENCION.COMPANIA 
       AND DETALLE_COMPROBANTE_CNT.ANO = COMPROBANTE_CNTRETENCION.ANO 
       AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = COMPROBANTE_CNTRETENCION.TIPO 
       AND DETALLE_COMPROBANTE_CNT.COMPROBANTE = COMPROBANTE_CNTRETENCION.NUMERO 
INNER JOIN RETENCIONES 
        ON COMPROBANTE_CNTRETENCION.COMPANIA = RETENCIONES.COMPANIA 
       AND COMPROBANTE_CNTRETENCION.ANO = RETENCIONES.ANO 
       AND COMPROBANTE_CNTRETENCION.TIPORETENCION = RETENCIONES.TIPO 
       AND COMPROBANTE_CNTRETENCION.CODIGORETENCION = RETENCIONES.CODIGO
     WHERE DETALLE_COMPROBANTE_CNT.COMPANIA = s$compania$s 
       AND RETENCIONES.CUENTA_CREDITO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
       AND DETALLE_COMPROBANTE_CNT.FECHA BETWEEN  s$fechaInicial$s AND s$fechaFinal$s
  GROUP BY RETENCIONES.CUENTA_CREDITO,RETENCIONES.CUENTA_CREDITO, RETENCIONES.TIPO, 
           RETENCIONES.CODIGO, COMPROBANTE_CNTRETENCION.TIPO, DETALLE_COMPROBANTE_CNT.FECHA, DETALLE_COMPROBANTE_CNT.COMPROBANTE, 
           TERCERO.NOMBRE, RETENCIONES.NOMBRE, DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE, RETENCIONES.PCT_APLICAR, 
           DETALLE_COMPROBANTE_CNT.ANO, DETALLE_COMPROBANTE_CNT.COMPANIA, DETALLE_COMPROBANTE_CNT.ANO
  ORDER BY DETALLE_COMPROBANTE_CNT.FECHA,DETALLE_COMPROBANTE_CNT.COMPROBANTE]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, 'MPEREZ' CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);