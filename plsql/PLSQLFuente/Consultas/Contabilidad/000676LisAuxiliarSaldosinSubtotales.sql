SELECT
  NOCOMP,
  NOANO,
  s$agrupa$s ID, 
  VD,
  VC,
  TIPO_CPTE,
  COMPROBANTE,
  CONSECUTIVO,
  FECHA,
  HORA,
  NODOCUMENTO,
  DESCRIP,
  NOMBRETERCERO,
  SALDOACUMULA,
  SALDOINICIAL,
  CASE WHEN INSTR('ILB',NVL(CLASE_CONTABLE,'I'))<>0 THEN 'E' ELSE 'S' END ORDENIMP,
  NOMBRECODIGO,
  SALDO
FROM
  (SELECT 
      V_PLAN_CONTABLE.COMPANIA NOCOMP,
      V_PLAN_CONTABLE.ANO NOANO,
      V_PLAN_CONTABLE.CODIGO CODIGO,
      V_PLAN_CONTABLE.ID ID,
      VALOR_DEBITO VD,
      VALOR_CREDITO VC, 
      DETALLE_COMPROBANTE_CNT.TIPO_CPTE,
      DETALLE_COMPROBANTE_CNT.COMPROBANTE,
      DETALLE_COMPROBANTE_CNT.CONSECUTIVO,
      DETALLE_COMPROBANTE_CNT.FECHA,
      DETALLE_COMPROBANTE_CNT.HORA,
      NVL(DETALLE_COMPROBANTE_CNT.NRO_DOCUMENTO,COMPROBANTE_CNT.NRO_DOCUMENTO) NODOCUMENTO,
      NVL(DETALLE_COMPROBANTE_CNT.DESCRIPCION,COMPROBANTE_CNT.DESCRIPCION) DESCRIP,
      TERCERO.NOMBRE NOMBRETERCERO,
      CASE WHEN VALOR_DEBITO=0 AND VALOR_CREDITO=0 THEN SUM(SALDOs$mesAnterior$s) ELSE 0 END + CASE WHEN V_PLAN_CONTABLE.NATURALEZA='D' THEN SUM(VALOR_DEBITO)-SUM(VALOR_CREDITO) ELSE SUM(VALOR_CREDITO)-SUM(VALOR_DEBITO) END SALDO,
      SUM (SUM(SALDOs$mesAnterior$s)+ CASE WHEN V_PLAN_CONTABLE.NATURALEZA='D' THEN SUM(VALOR_DEBITO)-SUM(VALOR_CREDITO) ELSE SUM(VALOR_CREDITO)-SUM(VALOR_DEBITO) END) OVER (PARTITION BY V_PLAN_CONTABLE.ID ORDER BY V_PLAN_CONTABLE.COMPANIA,V_PLAN_CONTABLE.ANO,V_PLAN_CONTABLE.CODIGO,DETALLE_COMPROBANTE_CNT.FECHA,CASE WHEN INSTR('ILB',TIPO_COMPROBANTE.CLASE_CONTABLE)<>0 THEN 'E' ELSE 'S' END,DETALLE_COMPROBANTE_CNT.TIPO_CPTE,DETALLE_COMPROBANTE_CNT.COMPROBANTE,DETALLE_COMPROBANTE_CNT.CONSECUTIVO ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) SALDOACUMULA,
      SUM(SALDOs$mesAnterior$s)+CASE WHEN V_PLAN_CONTABLE.NATURALEZA='D' THEN SUM(VALOR_DEBITO)-SUM(VALOR_CREDITO) ELSE SUM(VALOR_CREDITO)-SUM(VALOR_DEBITO) END SALDOINICIAL, 
      V_PLAN_CONTABLE.NOMBRE NOMBRECODIGO,
      TIPO_COMPROBANTE.CLASE_CONTABLE
    FROM DETALLE_COMPROBANTE_CNT
      LEFT JOIN V_PLAN_CONTABLE 
        ON  DETALLE_COMPROBANTE_CNT.COMPANIA    = V_PLAN_CONTABLE.COMPANIA  
        AND DETALLE_COMPROBANTE_CNT.ANO         = V_PLAN_CONTABLE.ANO 
        AND DETALLE_COMPROBANTE_CNT.CUENTA      = V_PLAN_CONTABLE.CODIGO
        AND DETALLE_COMPROBANTE_CNT.ID          = V_PLAN_CONTABLE.ID
      INNER JOIN COMPROBANTE_CNT 
        ON  DETALLE_COMPROBANTE_CNT.COMPANIA    = COMPROBANTE_CNT.COMPANIA 
        AND DETALLE_COMPROBANTE_CNT.ANO         = COMPROBANTE_CNT.ANO 
        AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE   = COMPROBANTE_CNT.TIPO 
        AND DETALLE_COMPROBANTE_CNT.COMPROBANTE = COMPROBANTE_CNT.NUMERO 
      INNER JOIN TIPO_COMPROBANTE 
        ON  DETALLE_COMPROBANTE_CNT.COMPANIA    = TIPO_COMPROBANTE.COMPANIA 
        AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE   = TIPO_COMPROBANTE.CODIGO 
      INNER JOIN CENTRO_COSTO  
       ON  DETALLE_COMPROBANTE_CNT.COMPANIA     = CENTRO_COSTO.COMPANIA 
       AND DETALLE_COMPROBANTE_CNT.ANO          = CENTRO_COSTO.ANO 
       AND DETALLE_COMPROBANTE_CNT.CENTRO_COSTO = CENTRO_COSTO.CODIGO 
      INNER JOIN TERCERO 
       ON  DETALLE_COMPROBANTE_CNT.COMPANIA     = TERCERO.COMPANIA 
       AND DETALLE_COMPROBANTE_CNT.TERCERO      = TERCERO.NIT 
       AND DETALLE_COMPROBANTE_CNT.SUCURSAL     = TERCERO.SUCURSAL
    WHERE V_PLAN_CONTABLE.COMPANIA=s$compania$s
      AND V_PLAN_CONTABLE.ANO=s$anio$s
      AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE BETWEEN 's$tipoInicial$s'  AND 's$tipoFinal$s' 
      AND V_PLAN_CONTABLE.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
      AND DETALLE_COMPROBANTE_CNT.FECHA BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
      s$filtrosTercero$s
      AND DETALLE_COMPROBANTE_CNT.CENTRO_COSTO BETWEEN 's$centroInicial$s' AND 's$centroFinal$s'  
      AND VALOR_DEBITO+VALOR_CREDITO<>0
    GROUP BY 
      V_PLAN_CONTABLE.COMPANIA, V_PLAN_CONTABLE.ANO, V_PLAN_CONTABLE.CODIGO, V_PLAN_CONTABLE.ID, VALOR_DEBITO, CLASE_CONTABLE,  V_PLAN_CONTABLE.NATURALEZA, 
      CASE WHEN INSTR('ILB',TIPO_COMPROBANTE.CLASE_CONTABLE)<>0 THEN 'E' ELSE 'S' END, VALOR_CREDITO, DETALLE_COMPROBANTE_CNT.TIPO_CPTE, DETALLE_COMPROBANTE_CNT.COMPROBANTE,
      DETALLE_COMPROBANTE_CNT.CONSECUTIVO, DETALLE_COMPROBANTE_CNT.FECHA, NVL(DETALLE_COMPROBANTE_CNT.NRO_DOCUMENTO,COMPROBANTE_CNT.NRO_DOCUMENTO), 
      NVL(DETALLE_COMPROBANTE_CNT.DESCRIPCION,COMPROBANTE_CNT.DESCRIPCION), TERCERO.NOMBRE, V_PLAN_CONTABLE.NOMBRE
    UNION ALL
    SELECT 
      V_PLAN_CONTABLE.COMPANIA NOCOMP,
      V_PLAN_CONTABLE.ANO NOANO,
      V_PLAN_CONTABLE.CODIGO CODIGO,
      V_PLAN_CONTABLE.ID ID,
      0 VD,
      0 VC,
      NULL TIPO_CPTE,
      NULL COMPROBANTE,
      NULL CONSECUTIVO,
      NULL FECHA,
      NULL HORA,
      NULL NODOCUMENTO,
      NULL DESCRIP,
      TERCERO.NOMBRE NOMBRETERCERO,
      V_PLAN_CONTABLE.SALDOs$mesAnterior$s SALDO,
      SUM(SALDOs$mesAnterior$s) SALDOACUMULA,
      SUM(SALDOs$mesAnterior$s) SALDOINICIAL,
      V_PLAN_CONTABLE.NOMBRE NOMBRECODIGO,
      NULL CLASE_CONTABLE
    FROM V_PLAN_CONTABLE 
      LEFT JOIN TERCERO 
        ON  V_PLAN_CONTABLE.COMPANIA = TERCERO.COMPANIA
        AND V_PLAN_CONTABLE.TERCERO = TERCERO.NIT
        AND V_PLAN_CONTABLE.SUCURSAL = TERCERO.SUCURSAL
    WHERE V_PLAN_CONTABLE.COMPANIA= s$compania$s
      AND V_PLAN_CONTABLE.ANO = s$anio$s
      AND V_PLAN_CONTABLE.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
      AND (V_PLAN_CONTABLE.SALDOs$mesAnterior$s <> 0 OR DEBITOs$mes$s + CREDITOs$mes$s <> 0)  
      AND  MOVIMIENTO NOT IN (0) 
    GROUP BY  
      V_PLAN_CONTABLE.COMPANIA, V_PLAN_CONTABLE.ANO, V_PLAN_CONTABLE.CODIGO, V_PLAN_CONTABLE.ID, 0, 0, NULL, NULL, NULL, NULL, 
      NULL, NULL, TERCERO.NOMBRE, V_PLAN_CONTABLE.SALDOs$mesAnterior$s, V_PLAN_CONTABLE.NOMBRE )
WHERE (FECHA BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')) 
  OR  (VD=0 AND VC=0 AND SALDOACUMULA<>0)
