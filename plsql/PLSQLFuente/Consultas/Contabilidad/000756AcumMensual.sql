SELECT CODIGO_CUENTA, 
       CUENTANOMBRE,
       SUM(VALOR_DEBITO) AS SUMADEVALOR_DEBITO, 
       SUM(VALOR_CREDITO) AS SUMADEVALOR_CREDITO
 FROM V_DETALLE_AUXILIAR_CNT  
WHERE COMPANIA   = s$compania$s
  AND ANO        = s$anio$s
  AND TIPO_CPTE  = 's$tipoCpte$s'
  AND TRUNC(FECHA) BETWEEN s$fechaInicial$s AND s$fechaFinal$s 
GROUP BY CODIGO_CUENTA, CUENTANOMBRE
ORDER BY CODIGO_CUENTA
