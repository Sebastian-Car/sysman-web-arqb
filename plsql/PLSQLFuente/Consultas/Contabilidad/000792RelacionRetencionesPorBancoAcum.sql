MERGE INTO CONSULTAS FIN USING (SELECT '000792RelacionRetencionesPorBancoAcum' INFORME ,TO_CLOB(q'[SELECT RETENCION.COMPANIA,
    SUM(RETENCION.VALOR_CREDITO) TOTALRETENCION, 
       RETENCION.BANCO_RETENCION    CODBANCO,
       BANCO.NOMBRE                 NOMBREBANCO,
       CASE WHEN 1<>s$resumido$s THEN CUENTARET.NOMBRE || '/' || NVL(TERCERO.NOMBRE,' ') ELSE CUENTARET.NOMBRE END  NOMBRECOMPLETO
FROM DETALLE_COMPROBANTE_CNT RETENCION INNER JOIN PLAN_CONTABLE BANCO
  ON RETENCION.COMPANIA        = BANCO.COMPANIA
 AND RETENCION.ANO             = BANCO.ANO
 AND RETENCION.BANCO_RETENCION = BANCO.CODIGO
INNER JOIN PLAN_CONTABLE CUENTARET
  ON RETENCION.COMPANIA     = CUENTARET.COMPANIA
 AND RETENCION.ANO          = CUENTARET.ANO
 AND RETENCION.CUENTA       = CUENTARET.CODIGO
INNER JOIN TERCERO   
  ON TERCERO.COMPANIA   = RETENCION.COMPANIA
 AND TERCERO.NIT        = RETENCION.TERCERO 
 AND TERCERO.SUCURSAL   = RETENCION.SUCURSAL
WHERE RETENCION.COMPANIA = s$compania$s
  AND RETENCION.BANCO_RETENCION BETWEEN 's$bancoInicial$s' AND 's$bancoFinal$s'
  AND RETENCION.FECHA_PAGO_RETENCION           BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND RETENCION.VALOR_CREDITO NOT IN(0)
  AND RETENCION.BANCO_RETENCION IS NOT NULL
  AND CUENTARET.CLASECUENTA IN('I')
GROUP BY RETENCION.COMPANIA,
     RETENCION.BANCO_RETENCION ,
         BANCO.NOMBRE,
         CASE WHEN 1<>s$resumido$s THEN CUENTARET.NOMBRE || '/' || NVL(TERCERO.NOMBRE,' ') ELSE CUENTARET.NOMBRE END
ORDER BY RETENCION.COMPANIA, RETENCION.BANCO_RETENCION, CASE WHEN 1<>s$resumido$s THEN CUENTARET.NOMBRE || '/' || NVL(TERCERO.NOMBRE,' ') ELSE CUENTARET.NOMBRE END]') CONSULTA, 1 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);