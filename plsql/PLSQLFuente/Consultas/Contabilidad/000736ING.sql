SELECT  V_DETALLE_AUXILIAR_CNT.TIPO_CPTE,
        V_DETALLE_AUXILIAR_CNT.TIPO_COMPROBANTE_NOM,
        V_DETALLE_AUXILIAR_CNT.COMPROBANTE,
        V_DETALLE_AUXILIAR_CNT.FECHA,
        V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO,
        V_DETALLE_AUXILIAR_CNT.TERCERO_COM TERCERO,
        V_DETALLE_AUXILIAR_CNT.NRO_DOCUMENTO,
        V_DETALLE_AUXILIAR_CNT.FECHA_VCN_DOC,  
        V_DETALLE_AUXILIAR_CNT.VLR_DOCUMENTO,  
        V_DETALLE_AUXILIAR_CNT.DESCRIPCION,
        V_DETALLE_AUXILIAR_CNT.TEXTO,          
        V_DETALLE_AUXILIAR_CNT.VLRAGIRAR, 
        V_DETALLE_AUXILIAR_CNT.TERCERO_COM_NOM TERCERO_NOM, 
        V_DETALLE_AUXILIAR_CNT.TERCERO_COM_DIR DIRECCION, 
        (CASE 
            WHEN V_DETALLE_AUXILIAR_CNT.TERCERO_COM_POS IS NULL 
                OR V_DETALLE_AUXILIAR_CNT.TERCERO_COM_POS = ''
            THEN PCK_SYSMAN_UTL.FC_PAR(V_DETALLE_AUXILIAR_CNT.COMPANIA,
                                      'CODIGO POSTAL',
                                      s$modulo$s,
                                      SYSDATE)
            ELSE V_DETALLE_AUXILIAR_CNT.TERCERO_COM_POS
        END) AS CODIGOPOSTAL,
        (CASE 
            WHEN PCK_SYSMAN_UTL.FC_PAR(V_DETALLE_AUXILIAR_CNT.COMPANIA,
                                      'CAMBIAR FIRMAS EN FORMATO ING',
                                      s$modulo$s,
                                      SYSDATE)= 'SI' 
            THEN 'Elaboró:'
            ELSE 'Recibió:'
        END) AS FIRMA1,
        (CASE 
            WHEN PCK_SYSMAN_UTL.FC_PAR(V_DETALLE_AUXILIAR_CNT.COMPANIA,
                                      'CAMBIAR FIRMAS EN FORMATO ING',
                                      s$modulo$s,
                                      SYSDATE)= 'SI' 
            THEN 'Revisó:'
            ELSE 'Aprobó:'
        END) AS FIRMA2,
        (CASE 
            WHEN V_DETALLE_AUXILIAR_CNT.VALOR_DEBITO <> 0
            THEN 'A' || V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA 
            ELSE 'Z' || V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA
        END) AS SEC,
        V_DETALLE_AUXILIAR_CNT.NRO_DOCUMENTO,
        V_DETALLE_AUXILIAR_CNT.CUENTAPPTAL CODIGO_PPTAL, 
        V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA,
        (TRIM(V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE) || 
        (CASE 
            WHEN V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO_DET = PCK_DATOS.FC_CONS_CENTRO()
            THEN '' 
            ELSE (' / '||V_DETALLE_AUXILIAR_CNT.CENTRO_COSTO_DET_NOM) 
        END) || 
        (CASE 
            WHEN V_DETALLE_AUXILIAR_CNT.TERCERO_DET = PCK_DATOS.FC_CONS_TERCERO() 
            THEN '' 
            ELSE (' / '||V_DETALLE_AUXILIAR_CNT.TERCERO_DET_NOM) 
        END)|| 
        (CASE 
            WHEN V_DETALLE_AUXILIAR_CNT.AUXILIAR_DET = PCK_DATOS.FC_CONS_AUXILIAR()
            THEN '' 
            ELSE (' / '||V_DETALLE_AUXILIAR_CNT.AUXILIAR_DET_NOM) 
        END) || 
        (CASE 
            WHEN V_DETALLE_AUXILIAR_CNT.REFERENCIA_DET = PCK_DATOS.FC_CONS_REFERENCIA() 
            THEN '' 
            ELSE (' / '||V_DETALLE_AUXILIAR_CNT.REFERENCIA_DET_NOM) 
        END)) AS TOTALNOMBRE,
        V_DETALLE_AUXILIAR_CNT.VALOR_DEBITO,
        V_DETALLE_AUXILIAR_CNT.VALOR_CREDITO
FROM V_DETALLE_AUXILIAR_CNT
WHERE V_DETALLE_AUXILIAR_CNT.COMPANIA     = s$compania$s
    AND V_DETALLE_AUXILIAR_CNT.ANO        = s$ano$s 
    AND V_DETALLE_AUXILIAR_CNT.TIPO_CPTE  = 's$tipoCpte$s' 
    AND V_DETALLE_AUXILIAR_CNT.COMPROBANTE BETWEEN 's$numeroPptoInicial$s' AND 's$numeroPptoFinal$s'
