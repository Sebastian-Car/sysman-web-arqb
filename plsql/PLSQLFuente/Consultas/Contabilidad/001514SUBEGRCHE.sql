SELECT 
   DETALLE.COMPANIA        AS COMP
  ,DETALLE.ANO             AS ANOCOMP
  ,DETALLE.TIPO_CPTE       AS TIPOCOMP
  ,DETALLE.COMPROBANTE     AS NUMCOMP
  ,DETALLE.TIPO_CPTE
  ,PLAN_PRESUPUESTAL.NOMBRE
  ,DETALLE.CUENTA          AS CUENTA
  ,DETALLE2.COMPROBANTE    AS NUMERORES
  ,DETALLE2.CMPTE_AFECTADO AS NUMERODIS
  ,DETALLE.VALOR_DEBITO - DETALLE.VALOR_CREDITO AS VALORNETO
  ,DETALLE2.TIPO_CPTE TIPO_CPTE2
  ,DETALLE2.COMPROBANTE AS NUMERORES1
  ,DETALLE2.TIPO_CPTE_AFECT TIPO_CPTE_AFECT2
  ,DETALLE2.CMPTE_AFECTADO AS NUMERODIS1
  ,DETALLE.CUENTA AS CUENTAID
  ,DETALLE1.TIPO_CPTE TIPO_CPTE1
  ,DETALLE1.COMPROBANTE AS NORES
  ,DETALLE1.TIPO_CPTE_AFECT TIPO_CPTE_AFECT1
  ,DETALLE1.CMPTE_AFECTADO AS NODIS
FROM DETALLE_COMPROBANTE_PPTAL DETALLE
  LEFT JOIN DETALLE_COMPROBANTE_PPTAL DETALLE1
	ON DETALLE.COMPANIA        = DETALLE1.COMPANIA
   AND DETALLE.ANO_AFECT       = DETALLE1.ANO
   AND DETALLE.TIPO_CPTE_AFECT = DETALLE1.TIPO_CPTE
   AND DETALLE.CMPTE_AFECTADO  = DETALLE1.COMPROBANTE
   AND DETALLE.CONSECUTIVOPPTO = DETALLE1.CONSECUTIVO
  LEFT JOIN DETALLE_COMPROBANTE_PPTAL DETALLE2 
    ON DETALLE1.COMPANIA        = DETALLE2.COMPANIA
   AND DETALLE1.ANO_AFECT       = DETALLE2.ANO
   AND DETALLE1.TIPO_CPTE_AFECT = DETALLE2.TIPO_CPTE
   AND DETALLE1.CMPTE_AFECTADO  = DETALLE2.COMPROBANTE
   AND DETALLE1.CONSECUTIVOPPTO = DETALLE2.CONSECUTIVO
  INNER JOIN PLAN_PRESUPUESTAL 
    ON DETALLE.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
   AND DETALLE.ANO      = PLAN_PRESUPUESTAL.ANO
   AND DETALLE.CUENTA   = PLAN_PRESUPUESTAL.CODIGO
WHERE DETALLE.COMPANIA     = $P{PR_COMP}
  AND DETALLE.ANO                   = $P{PR_ANOCOMP}
  AND DETALLE.TIPO_CPTE        = $P{PR_TIPOCOMP}
  AND DETALLE.COMPROBANTE = $P{PR_NUMCOMP}
