WITH BALANCESALDOSINICIALESNIVELES1 AS 
(
    SELECT 
      SALDOSINICIALES.COMPANIA,
      SALDOSINICIALES.ANO,
      SALDOSINICIALES.CODIGO SALDOS_INICIALES_CODIGO,  
      CASE WHEN SALDOSINICIALES.AUXILIAR IS NOT NULL 
           THEN AUXILIAR.NOMBRE
           ELSE CASE WHEN SALDOSINICIALES.TERCERO IS NOT NULL 
                     THEN TERCERO.NOMBRE 
                     ELSE CASE WHEN SALDOSINICIALES.CENTRO_COSTO IS NOT NULL 
                               THEN CENTRO_COSTO.NOMBRE 
                               ELSE PLAN_CONTABLE.NOMBRE 
                          END
                END
      END NOMBRE,         
      SALDOSINICIALES.SALDOINICIAL, 
      SUBSTR(SALDOSINICIALES.CODIGO,1,1) CLASE,
      SUBSTR(SALDOSINICIALES.CODIGO,1,2)  GRUPO,
      SUBSTR(SALDOSINICIALES.CODIGO,1,4)  CUENTA,
      SUBSTR(SALDOSINICIALES.CODIGO,1,6)  SUBCUENTA, 
      SALDOSINICIALES.TERCERO,
      CENTRO_COSTO.CODIGO CENTRO_COSTO_CODIGO,
      AUXILIAR.CODIGO AUXILIAR_CODIGO   
    FROM SALDOSINICIALES
	   INNER JOIN PLAN_CONTABLE
	     ON SALDOSINICIALES.COMPANIA = PLAN_CONTABLE.COMPANIA
	    AND SALDOSINICIALES.ANO      = PLAN_CONTABLE.ANO
	    AND SALDOSINICIALES.CODIGO   = PLAN_CONTABLE.CODIGO
	   INNER JOIN TERCERO
	     ON SALDOSINICIALES.COMPANIA = TERCERO.COMPANIA
	    AND SALDOSINICIALES.TERCERO  = TERCERO.NIT
	    AND SALDOSINICIALES.SUCURSAL = TERCERO.SUCURSAL
	   INNER JOIN CENTRO_COSTO
	     ON SALDOSINICIALES.COMPANIA      = CENTRO_COSTO.COMPANIA
	    AND SALDOSINICIALES.ANO          = CENTRO_COSTO.ANO
	    AND SALDOSINICIALES.CENTRO_COSTO = CENTRO_COSTO.CODIGO
	   INNER JOIN AUXILIAR
	     ON SALDOSINICIALES.COMPANIA     = AUXILIAR.COMPANIA
	    AND SALDOSINICIALES.ANO          = AUXILIAR.ANO
	    AND SALDOSINICIALES.AUXILIAR     = AUXILIAR.CODIGO
    WHERE SALDOSINICIALES.COMPANIA = s$compania$s 
      AND SALDOSINICIALES.ANO        = s$anio$s
    ORDER BY SALDOSINICIALES.CODIGO,    
      CASE WHEN SALDOSINICIALES.AUXILIAR IS NOT NULL 
           THEN AUXILIAR.NOMBRE
           ELSE CASE WHEN SALDOSINICIALES.TERCERO IS NOT NULL 
                     THEN TERCERO.NOMBRE 
                     ELSE CASE WHEN SALDOSINICIALES.CENTRO_COSTO IS NOT NULL 
                               THEN CENTRO_COSTO.NOMBRE 
                               ELSE PLAN_CONTABLE.NOMBRE 
                          END
                END
      END 
  )
SELECT 
  BALANCESALDOSINICIALESNIVELES1.SALDOS_INICIALES_CODIGO,
  BALANCESALDOSINICIALESNIVELES1.NOMBRE,
  BALANCESALDOSINICIALESNIVELES1.SALDOINICIAL,
  BALANCESALDOSINICIALESNIVELES1.CLASE,
  BALANCESALDOSINICIALESNIVELES1.GRUPO,
  BALANCESALDOSINICIALESNIVELES1.CUENTA,
  BALANCESALDOSINICIALESNIVELES1.SUBCUENTA,
  PLAN_CONTABLE.NOMBRE  NOMBRECLASE,
  PLAN_CONTABLE_1.NOMBRE NOMBRE1,
  PLAN_CONTABLE_2.NOMBRE NOMBRE2,
  PLAN_CONTABLE_3.NOMBRE NOMBRE3 
FROM     
 BALANCESALDOSINICIALESNIVELES1
	LEFT JOIN PLAN_CONTABLE
	  ON BALANCESALDOSINICIALESNIVELES1.COMPANIA = PLAN_CONTABLE.COMPANIA
	 AND BALANCESALDOSINICIALESNIVELES1.ANO     = PLAN_CONTABLE.ANO
	 AND BALANCESALDOSINICIALESNIVELES1.CLASE   = PLAN_CONTABLE.CODIGO
	LEFT JOIN PLAN_CONTABLE PLAN_CONTABLE_1
	  ON BALANCESALDOSINICIALESNIVELES1.COMPANIA = PLAN_CONTABLE_1.COMPANIA
	 AND BALANCESALDOSINICIALESNIVELES1.ANO      = PLAN_CONTABLE_1.ANO
	 AND BALANCESALDOSINICIALESNIVELES1.GRUPO     = PLAN_CONTABLE_1.CODIGO
	LEFT JOIN PLAN_CONTABLE PLAN_CONTABLE_2
	  ON BALANCESALDOSINICIALESNIVELES1.COMPANIA = PLAN_CONTABLE_2.COMPANIA
	 AND BALANCESALDOSINICIALESNIVELES1.ANO      = PLAN_CONTABLE_2.ANO
	 AND BALANCESALDOSINICIALESNIVELES1.CUENTA    = PLAN_CONTABLE_2.CODIGO
	LEFT JOIN PLAN_CONTABLE PLAN_CONTABLE_3
	  ON BALANCESALDOSINICIALESNIVELES1.COMPANIA = PLAN_CONTABLE_3.COMPANIA
	 AND BALANCESALDOSINICIALESNIVELES1.ANO      = PLAN_CONTABLE_3.ANO
	 AND BALANCESALDOSINICIALESNIVELES1.SUBCUENTA = PLAN_CONTABLE_3.CODIGO
