SELECT  
  COMPROBANTE_CNT.COMPANIA, 
  COMPROBANTE_CNT.ANO, 
  COMPROBANTE_CNT.TIPO, 
  COMPROBANTE_CNT.NUMERO, 
  COMPROBANTE_CNT.FECHA, 
  COMPROBANTE_CNT.FECHARECEPCION, 
  COMPROBANTE_CNT.FECHA_VCN_DOC, 
  COMPROBANTE_CNT.NRO_DOCUMENTO NODOC,  
  VLRAGIRAR-CASE WHEN DEBITOSAFECTADOS>0 AND DEBITOSAFECTADOS=CREDITOSAFECTADOS THEN DEBITOSAFECTADOS ELSE DEBITOSAFECTADOS-CREDITOSAFECTADOS END +ABONADO TOTALDEUDA, 
  COMPROBANTE_CNT.DEBITOSAFECTADOS, 
  COMPROBANTE_CNT.CREDITOSAFECTADOS, 
  COMPROBANTE_CNT.ABONADO, 
  COMPROBANTE_CNT.VLRAGIRAR, 
  VLRAGIRAR-CASE WHEN DEBITOSAFECTADOS>0 AND DEBITOSAFECTADOS=CREDITOSAFECTADOS THEN DEBITOSAFECTADOS ELSE DEBITOSAFECTADOS-CREDITOSAFECTADOS END +ABONADO SALDO, 
  COMPROBANTE_CNT.TERCERO, 
  COMPROBANTE_CNT.SUCURSAL,  
  TERCERO.NOMBRE NOMBRETERCERO, 
  TERCERO.TELEFONOS,  
  TERCERO.DIRECCION, 
  TO_DATE('s$fechaCorte$s','DD/MM/YYYY') - COMPROBANTE_CNT.s$porVencimiento$s DIFERENCIAENDIAS, 
  CASE WHEN TO_DATE('s$fechaCorte$s','DD/MM/YYYY') - TRUNC(COMPROBANTE_CNT.s$porVencimiento$s) <=0 THEN VLRAGIRAR-CASE WHEN DEBITOSAFECTADOS>0 AND DEBITOSAFECTADOS=CREDITOSAFECTADOS THEN DEBITOSAFECTADOS ELSE DEBITOSAFECTADOS-CREDITOSAFECTADOS END +ABONADO ELSE 0 END POR_VENCER, 
  CASE WHEN TO_DATE('s$fechaCorte$s','DD/MM/YYYY') - TRUNC(COMPROBANTE_CNT.s$porVencimiento$s) <=0 THEN VLRAGIRAR-CASE WHEN DEBITOSAFECTADOS>0 AND DEBITOSAFECTADOS=CREDITOSAFECTADOS THEN DEBITOSAFECTADOS ELSE DEBITOSAFECTADOS-CREDITOSAFECTADOS END +ABONADO ELSE 0 END SALDO0, 
  CASE WHEN TO_DATE('s$fechaCorte$s','DD/MM/YYYY') - TRUNC(COMPROBANTE_CNT.s$porVencimiento$s) BETWEEN 1 AND 30 THEN VLRAGIRAR-CASE WHEN DEBITOSAFECTADOS>0 AND DEBITOSAFECTADOS=CREDITOSAFECTADOS THEN DEBITOSAFECTADOS ELSE DEBITOSAFECTADOS-CREDITOSAFECTADOS END +ABONADO ELSE 0 END SALDO1, 
  CASE WHEN TO_DATE('s$fechaCorte$s','DD/MM/YYYY') - TRUNC(COMPROBANTE_CNT.s$porVencimiento$s) BETWEEN 31 AND 60 THEN VLRAGIRAR-CASE WHEN DEBITOSAFECTADOS>0 AND DEBITOSAFECTADOS=CREDITOSAFECTADOS THEN DEBITOSAFECTADOS ELSE DEBITOSAFECTADOS-CREDITOSAFECTADOS END +ABONADO ELSE 0 END SALDO2, 
  CASE WHEN TO_DATE('s$fechaCorte$s','DD/MM/YYYY') - TRUNC(COMPROBANTE_CNT.s$porVencimiento$s) BETWEEN 61 AND 90 THEN VLRAGIRAR-CASE WHEN DEBITOSAFECTADOS>0 AND DEBITOSAFECTADOS=CREDITOSAFECTADOS THEN DEBITOSAFECTADOS ELSE DEBITOSAFECTADOS-CREDITOSAFECTADOS END +ABONADO ELSE 0 END SALDO3, 
  CASE WHEN TO_DATE('s$fechaCorte$s','DD/MM/YYYY') - TRUNC(COMPROBANTE_CNT.s$porVencimiento$s) >90 THEN VLRAGIRAR-CASE WHEN DEBITOSAFECTADOS>0 AND DEBITOSAFECTADOS=CREDITOSAFECTADOS THEN DEBITOSAFECTADOS ELSE DEBITOSAFECTADOS-CREDITOSAFECTADOS END +ABONADO ELSE 0 END SALDO4, 
  COMPROBANTE_CNT.TEXTO, 
  COMPROBANTE_CNT.DESCRIPCION 
FROM COMPROBANTE_CNT  
  INNER JOIN TIPO_COMPROBANTE  
     ON COMPROBANTE_CNT.TIPO     = TIPO_COMPROBANTE.CODIGO 
    AND COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA 
  INNER JOIN TERCERO  
     ON COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL  
    AND COMPROBANTE_CNT.TERCERO  = TERCERO.NIT 
    AND COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA 
WHERE COMPROBANTE_CNT.COMPANIA = s$compania$s 
  AND COMPROBANTE_CNT.FECHARECEPCION <= TO_DATE('s$fechaCorte$s','DD/MM/YYYY')  
  AND VLRAGIRAR-CASE WHEN DEBITOSAFECTADOS>0 AND DEBITOSAFECTADOS=CREDITOSAFECTADOS THEN DEBITOSAFECTADOS ELSE DEBITOSAFECTADOS-CREDITOSAFECTADOS END +ABONADO>0  
  AND TERCERO.NIT BETWEEN 's$terceroInicial$s' AND 's$terceroFinal$s' 
  AND TIPO_COMPROBANTE.CLASE_CONTABLE='P' 
  s$soloAprobados$s
