SELECT * 
FROM(
WITH COMANULADOS AS
  ( 
SELECT DISTINCT DETALLE_COMPROBANTE_CNT.TIPO_CPTE_AFECT,
    DETALLE_COMPROBANTE_CNT.CMPTE_AFECTADO,
    DETALLE_COMPROBANTE_CNT.COMPANIA,
    DETALLE_COMPROBANTE_CNT.ANO
  FROM PLAN_CONTABLE
  INNER JOIN DETALLE_COMPROBANTE_CNT
   ON PLAN_CONTABLE.COMPANIA = DETALLE_COMPROBANTE_CNT.COMPANIA
   AND PLAN_CONTABLE.ANO     = DETALLE_COMPROBANTE_CNT.ANO
   AND PLAN_CONTABLE.CODIGO  = DETALLE_COMPROBANTE_CNT.CUENTA
  INNER JOIN TIPO_COMPROBANTE
   ON DETALLE_COMPROBANTE_CNT.COMPANIA   = TIPO_COMPROBANTE.COMPANIA
   AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
  WHERE DETALLE_COMPROBANTE_CNT.COMPANIA        = s$compania$s 
    AND DETALLE_COMPROBANTE_CNT.ANO             = s$anio$s
    AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE_AFECT IN ('COM')
    AND TIPO_COMPROBANTE.CLASE_CONTABLE         IN ('T')
    AND PLAN_CONTABLE.CLASECUENTA               IN ('P')
  )
SELECT DETALLE_COMPROBANTE_CNT.TERCERO, 
       TERCERO.NOMBRE                 TERCERO_NOMBRE, 
       DETALLE_COMPROBANTE_CNT.CUENTA CODIGO_CUENTA,  
       SUM(NVL(DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE,0)) BASE_GRAVABLE,
       SUM(CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='E'
                THEN NVL(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO,0)
                ELSE NVL(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO,0)-NVL(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO,0)
                END)VALOR_NETO,
       PLAN_CONTABLE.NOMBRE NOMBRECUENTA, 
       PCK_SYSMAN_UTL.FC_ROUND(DETALLE_COMPROBANTE_CNT.MES/s$cPeriodo$s,0) PERIODO, 
      CASE PCK_SYSMAN_UTL.FC_ROUND(DETALLE_COMPROBANTE_CNT.MES/s$cPeriodo$s,0)  
               WHEN 0  THEN  'PRIMER'
               WHEN 1  THEN  'PRIMER' 
               WHEN 2  THEN  'SEGUNDO' 
               WHEN 3  THEN  'TERCER' 
               WHEN 4  THEN  'CUARTO' 
               WHEN 5  THEN  'QUINTO' 
               WHEN 6  THEN  'SEXTO' 
               WHEN 7  THEN  'SEPTIMO' 
               WHEN 8  THEN  'OCTAVO' 
               WHEN 9  THEN  'NOVENO' 
               WHEN 10 THEN  'DECIMO'            
             END NOCARDINALESPERIODO
FROM DETALLE_COMPROBANTE_CNT 
INNER JOIN TERCERO 
  ON  DETALLE_COMPROBANTE_CNT.COMPANIA  = TERCERO.COMPANIA
  AND DETALLE_COMPROBANTE_CNT.TERCERO   = TERCERO.NIT  
  AND DETALLE_COMPROBANTE_CNT.SUCURSAL  = TERCERO.SUCURSAL 
INNER JOIN TIPO_COMPROBANTE
  ON  DETALLE_COMPROBANTE_CNT.COMPANIA  = TIPO_COMPROBANTE.COMPANIA 
  AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO  
INNER JOIN PLAN_CONTABLE
  ON  DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA
  AND DETALLE_COMPROBANTE_CNT.ANO     = PLAN_CONTABLE.ANO
  AND DETALLE_COMPROBANTE_CNT.CUENTA  = PLAN_CONTABLE.CODIGO
LEFT JOIN COMANULADOS
ON DETALLE_COMPROBANTE_CNT.COMPANIA        = COMANULADOS.COMPANIA
AND DETALLE_COMPROBANTE_CNT.ANO            = COMANULADOS.ANO
AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE      = COMANULADOS.TIPO_CPTE_AFECT
AND DETALLE_COMPROBANTE_CNT.COMPROBANTE    = COMANULADOS.CMPTE_AFECTADO
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA   = s$compania$s
  AND DETALLE_COMPROBANTE_CNT.ANO        = s$anio$s 
  AND DETALLE_COMPROBANTE_CNT.FECHA   BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE( 's$fechaFinal$s'
,'DD/MM/YYYY')
  AND s$filtroNitONombre$s   BETWEEN 's$tercero1$s' AND 's$tercero2$s' 
  AND DETALLE_COMPROBANTE_CNT.CUENTA BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s' 
  AND TIPO_COMPROBANTE.CLASE_CONTABLE NOT IN('Z','T') 
  AND PLAN_CONTABLE.CLASECUENTA           IN ('I')
  AND COMANULADOS.CMPTE_AFECTADO          IS NULL
GROUP BY DETALLE_COMPROBANTE_CNT.TIPO_CPTE,
         DETALLE_COMPROBANTE_CNT.COMPROBANTE, 
         DETALLE_COMPROBANTE_CNT.TERCERO, 
         TERCERO.NOMBRE, 
         DETALLE_COMPROBANTE_CNT.CUENTA, 
         DETALLE_COMPROBANTE_CNT.COMPANIA, 
         DETALLE_COMPROBANTE_CNT.ANO, 
         PCK_SYSMAN_UTL.FC_ROUND(DETALLE_COMPROBANTE_CNT.MES/s$cPeriodo$s,0), 
         DETALLE_COMPROBANTE_CNT.COMPANIA,
         DETALLE_COMPROBANTE_CNT.ANO,
         PLAN_CONTABLE.NOMBRE, 
         PLAN_CONTABLE.CLASECUENTA,
        CASE PCK_SYSMAN_UTL.FC_ROUND(DETALLE_COMPROBANTE_CNT.MES/s$cPeriodo$s,0)  
               WHEN 0  THEN  'PRIMER'
               WHEN 1  THEN  'PRIMER' 
               WHEN 2  THEN  'SEGUNDO' 
               WHEN 3  THEN  'TERCER' 
               WHEN 4  THEN  'CUARTO' 
               WHEN 5  THEN  'QUINTO' 
               WHEN 6  THEN  'SEXTO' 
               WHEN 7  THEN  'SEPTIMO' 
               WHEN 8  THEN  'OCTAVO' 
               WHEN 9  THEN  'NOVENO' 
               WHEN 10 THEN  'DECIMO'            
             END
) CONPERIODO
WHERE s$conPeriodo$s = 1 
UNION ALL 
SELECT * 
FROM
(
WITH COMANULADOS AS
  ( 
SELECT DISTINCT DETALLE_COMPROBANTE_CNT.TIPO_CPTE_AFECT,
    DETALLE_COMPROBANTE_CNT.CMPTE_AFECTADO,
    DETALLE_COMPROBANTE_CNT.COMPANIA,
    DETALLE_COMPROBANTE_CNT.ANO
  FROM PLAN_CONTABLE
  INNER JOIN DETALLE_COMPROBANTE_CNT
   ON PLAN_CONTABLE.COMPANIA = DETALLE_COMPROBANTE_CNT.COMPANIA
   AND PLAN_CONTABLE.ANO     = DETALLE_COMPROBANTE_CNT.ANO
   AND PLAN_CONTABLE.CODIGO  = DETALLE_COMPROBANTE_CNT.CUENTA
  INNER JOIN TIPO_COMPROBANTE
   ON DETALLE_COMPROBANTE_CNT.COMPANIA   = TIPO_COMPROBANTE.COMPANIA
   AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
  WHERE DETALLE_COMPROBANTE_CNT.COMPANIA        = s$compania$s 
    AND DETALLE_COMPROBANTE_CNT.ANO             = s$anio$s
    AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE_AFECT IN ('COM')
    AND TIPO_COMPROBANTE.CLASE_CONTABLE         IN ('T')
    AND PLAN_CONTABLE.CLASECUENTA               IN ('P')
  )
SELECT DETALLE_COMPROBANTE_CNT.TERCERO TERCERO,
       TERCERO.NOMBRE                  TERCERO_NOMBRE,
       DETALLE_COMPROBANTE_CNT.CUENTA CODIGO_CUENTA,
       NVL(DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE,0) BASE_GRAVABLE,
       CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='E'
            THEN NVL(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO,0)
            ELSE NVL(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO,0)-NVL(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO,0)
            END VALOR_NETO, 
       PLAN_CONTABLE.NOMBRE NOMBRECUENTA,
       ROUND(DETALLE_COMPROBANTE_CNT.MES/s$cPeriodo$s,0) PERIODO,
       PCK_SYSMAN_UTL.FC_NOCARDINALES(PCK_SYSMAN_UTL.FC_ROUND(DETALLE_COMPROBANTE_CNT.MES/s$cPeriodo$s,0),1) NOCARDINALESPERIODO
FROM DETALLE_COMPROBANTE_CNT
  INNER JOIN TERCERO
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.TERCERO  = TERCERO.NIT
    AND DETALLE_COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL
  INNER JOIN TIPO_COMPROBANTE
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA  = TIPO_COMPROBANTE.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE = TIPO_COMPROBANTE.CODIGO
  INNER JOIN PLAN_CONTABLE
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.ANO      = PLAN_CONTABLE.ANO
    AND DETALLE_COMPROBANTE_CNT.CUENTA   = PLAN_CONTABLE.CODIGO
  LEFT JOIN COMANULADOS
    ON DETALLE_COMPROBANTE_CNT.COMPANIA        = COMANULADOS.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.ANO            = COMANULADOS.ANO
    AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE      = COMANULADOS.TIPO_CPTE_AFECT
    AND DETALLE_COMPROBANTE_CNT.COMPROBANTE    = COMANULADOS.CMPTE_AFECTADO  
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA = s$compania$s
  AND DETALLE_COMPROBANTE_CNT.ANO      = s$anio$s
  AND DETALLE_COMPROBANTE_CNT.FECHA    BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE( 's$fechaFinal$s'
,'DD/MM/YYYY')
  AND  s$filtroNitONombre$s     BETWEEN 's$tercero1$s' AND 's$tercero2$s'
  AND DETALLE_COMPROBANTE_CNT.CUENTA   BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  AND TIPO_COMPROBANTE.CLASE_CONTABLE  NOT IN('Z','T')
  AND NVL(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO,0)  NOT IN (0)
  AND NVL(DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE,0)  NOT IN (0)
  AND PLAN_CONTABLE.CLASECUENTA           IN ('I')
  AND COMANULADOS.CMPTE_AFECTADO          IS NULL) DISCRIMINADO
WHERE s$conPeriodoDiscriminado$s = 2
