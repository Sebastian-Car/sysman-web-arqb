SELECT 
  DETALLE.COMPANIA,
  DETALLE.CUENTA AS CODIGO_CUENTA,
  SUM(DETALLE.VALOR_DEBITO) AS SUMADEVALOR_DEBITO,
  SUM(DETALLE.VALOR_CREDITO) AS SUMADEVALOR_CREDITO,
  MAX(PLANC.NOMBRE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS ULTIMODENOMBRE,
  MAX(PLANP.NOMBRE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS ULTIMODENOMBRE1,
  MAX(PLANP.CODIGO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS ULTIMODECODIGO,
  TIPOC.CLASE_CONTABLE
FROM DETALLE_COMPROBANTE_CNT DETALLE
  LEFT JOIN PLAN_CONTABLE PLANC
     ON DETALLE.COMPANIA = PLANC.COMPANIA
    AND DETALLE.ANO      = PLANC.ANO
    AND DETALLE.CUENTA   = PLANC.CODIGO
  LEFT JOIN PLAN_PRESUPUESTAL PLANP
     ON PLANC.COMPANIA     = PLANP.COMPANIA
    AND PLANC.ANO          = PLANP.ANO
    AND PLANC.CUENTA_PPTAL = PLANP.CODIGO
  LEFT JOIN TIPO_COMPROBANTE TIPOC
     ON DETALLE.COMPANIA   = TIPOC.COMPANIA
    AND DETALLE.TIPO_CPTE  = TIPOC.CODIGO
WHERE DETALLE.COMPANIA = '$P!{PR_COMPANIA}'
  AND DETALLE.FECHA    = TO_DATE( '$P!{PR_FECHA}', 'DD/MM/YYYY')
  AND SUBSTR(DETALLE.CUENTA, 2) >= 12
  AND TIPOC.CLASE_CONTABLE = 'I'
  OR TIPOC.CLASE_CONTABLE = 'S'
  OR TIPOC.CLASE_CONTABLE = 'B'
  GROUP BY
  DETALLE.CUENTA,
  DETALLE.COMPANIA,
  TIPOC.CLASE_CONTABLE
  HAVING
  TIPOC.CLASE_CONTABLE = 'S'
  OR TIPOC.CLASE_CONTABLE = 'I'
  OR TIPOC.CLASE_CONTABLE = 'B'
