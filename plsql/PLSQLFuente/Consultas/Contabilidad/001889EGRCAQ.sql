SELECT V_DETALLE_AUXILIAR_CNT.ANO ANOCOMP,
  CASE
    WHEN V_DETALLE_AUXILIAR_CNT.CODIGOPOSTAL_DET IS NULL
    OR V_DETALLE_AUXILIAR_CNT.CODIGOPOSTAL_DET    = ''
    THEN PCK_SYSMAN_UTL.FC_PAR(V_DETALLE_AUXILIAR_CNT.COMPANIA,'CODIGO POSTAL',3, SYSDATE, -1)
    ELSE V_DETALLE_AUXILIAR_CNT.CODIGOPOSTAL_DET
  END CODIGOPOSTAL,
  V_DETALLE_AUXILIAR_CNT.COMPANIA,
  V_DETALLE_AUXILIAR_CNT.CMPTE_AFECTADO_DET COMPAFECT,
  V_DETALLE_AUXILIAR_CNT.DESCRIPCION,
  V_DETALLE_AUXILIAR_CNT.FECHA_COMPROBANTE,
  V_DETALLE_AUXILIAR_CNT.TERCERO_COM_NOM NOMBRECOMPLETO,
  V_DETALLE_AUXILIAR_CNT.COMPROBANTE NUMCOMP,
  V_DETALLE_AUXILIAR_CNT.COMPROBANTE NUMERO,
  V_DETALLE_AUXILIAR_CNT.TEXTO,
  V_DETALLE_AUXILIAR_CNT.TIPO_CPTE_AFECT_DET TIPOAFECT,
  V_DETALLE_AUXILIAR_CNT.TIPO_CPTE TIPOCOMP,
  COMPROBANTE_CNT.TIPOPAGO,
  V_DETALLE_AUXILIAR_CNT.CUENTANOMBRE TOTALNOMBRE,
  V_DETALLE_AUXILIAR_CNT.VLRAGIRAR,
  V_DETALLE_AUXILIAR_CNT.DIGITOVERIFICACION,
  V_DETALLE_AUXILIAR_CNT.TIPO_CPTE TIPO,
  V_DETALLE_AUXILIAR_CNT.CODIGO_CUENTA ,
  V_DETALLE_AUXILIAR_CNT.TERCERO_COM NITTERCERO,
  V_DETALLE_AUXILIAR_CNT.VALOR_DEBITO,
  V_DETALLE_AUXILIAR_CNT.VALOR_CREDITO
FROM V_DETALLE_AUXILIAR_CNT
INNER JOIN COMPROBANTE_CNT
	ON V_DETALLE_AUXILIAR_CNT.COMPANIA     = COMPROBANTE_CNT.COMPANIA
	AND V_DETALLE_AUXILIAR_CNT.ANO         = COMPROBANTE_CNT.ANO
	AND V_DETALLE_AUXILIAR_CNT.TIPO_CPTE   = COMPROBANTE_CNT.TIPO
	AND V_DETALLE_AUXILIAR_CNT.COMPROBANTE = COMPROBANTE_CNT.NUMERO
WHERE V_DETALLE_AUXILIAR_CNT.COMPANIA    = s$compania$s
  AND V_DETALLE_AUXILIAR_CNT.ANO         = s$ano$s
  AND V_DETALLE_AUXILIAR_CNT.TIPO_CPTE   = 's$tipoCpte$s'
  AND V_DETALLE_AUXILIAR_CNT.COMPROBANTE BETWEEN s$numeroPptoInicial$s AND s$numeroPptoFinal$s
