WITH 
    BASE AS 
        (SELECT COMPROBANTE_CNTRETENCION.COMPANIA,
                COMPROBANTE_CNTRETENCION.ANO,
                COMPROBANTE_CNTRETENCION.TIPO,
                COMPROBANTE_CNTRETENCION.NUMERO,
                COMPROBANTE_CNTRETENCION.VALOR      AS VALORRETENIDO,
                (CASE 
                    WHEN ALEY1450 NOT IN (0) 
                        THEN PCT_APLICARLEY1607 
                    ELSE RETENCIONES.PCT_APLICAR 
                END)                                AS PCT_APLICAR,
                (CASE 
                    WHEN ALEY1450 NOT IN (0) 
                        THEN VALORBASELEY1607 
                    ELSE COMPROBANTE_CNTRETENCION.VALORBASE 
                END)                                AS VALORBASE,
                COMPROBANTE_CNT.PORCIVA,
                COMPROBANTE_CNT.NRO_DOCUMENTO,
                TERCERO.NOMBRE,
                TERCERO.NIT,
                COMPROBANTE_CNT.DESCRIPCION,
                COMPROBANTE_CNT.FECHA,
                COMPROBANTE_CNT.CREATED_BY 
            FROM 
            (COMPROBANTE_CNT LEFT JOIN 
                (COMPROBANTE_CNTRETENCION LEFT JOIN TIPORETENCION ON 
                    COMPROBANTE_CNTRETENCION.TIPORETENCION = TIPORETENCION.CODIGO) ON 
                    COMPROBANTE_CNT.COMPANIA    = COMPROBANTE_CNTRETENCION.COMPANIA 
                        AND COMPROBANTE_CNT.ANO     = COMPROBANTE_CNTRETENCION.ANO 
                        AND COMPROBANTE_CNT.TIPO    = COMPROBANTE_CNTRETENCION.TIPO 
                        AND (COMPROBANTE_CNT.NUMERO = COMPROBANTE_CNTRETENCION.NUMERO) LEFT JOIN TERCERO ON 
                    COMPROBANTE_CNT.COMPANIA  = TERCERO.COMPANIA 
                    AND COMPROBANTE_CNT.TERCERO  = TERCERO.NIT 
                    AND COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL) LEFT JOIN RETENCIONES ON 
                COMPROBANTE_CNTRETENCION.COMPANIA         = RETENCIONES.COMPANIA
                AND COMPROBANTE_CNTRETENCION.ANO             = RETENCIONES.ANO 
                AND COMPROBANTE_CNTRETENCION.TIPORETENCION   = RETENCIONES.TIPO 
                AND COMPROBANTE_CNTRETENCION.CODIGORETENCION = RETENCIONES.CODIGO 
            WHERE COMPROBANTE_CNTRETENCION.COMPANIA = s$compania$s
                AND COMPROBANTE_CNTRETENCION.ANO        = s$ano$s
                AND COMPROBANTE_CNTRETENCION.TIPO       = 's$tipoCpte$s'
                AND COMPROBANTE_CNTRETENCION.NUMERO     = s$numeroCpte$s
                AND TIPORETENCION.CODIGO                IN ('IVA')
            ORDER BY COMPROBANTE_CNTRETENCION.COMPANIA,
                COMPROBANTE_CNTRETENCION.ANO,
                COMPROBANTE_CNTRETENCION.TIPO,
                COMPROBANTE_CNTRETENCION.NUMERO)
SELECT  COMPANIA,
        ANO,
        TIPO,
        NUMERO,
        VALORRETENIDO,
        PCT_APLICAR,
        VALORBASE,
        PORCIVA,
        NRO_DOCUMENTO,
        NOMBRE,
        NIT,
        DESCRIPCION,
        FECHA,
        CREATED_BY, 
        ROUND(VALORBASE*PORCIVA/100,0) AS IVAGENERADO
FROM BASE
