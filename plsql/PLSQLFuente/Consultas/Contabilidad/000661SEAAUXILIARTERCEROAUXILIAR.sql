SELECT 
  DETALLE_COMPROBANTE_CNT.COMPANIA,
  DETALLE_COMPROBANTE_CNT.ANO,
  DETALLE_COMPROBANTE_CNT.CUENTA CODIGO_CUENTA,
  DETALLE_COMPROBANTE_CNT.FUENTE_RECURSO AUXILIAR,
  FUENTE_RECURSOS.NOMBRE AUXILIAR_NOM,
  PLAN_CONTABLE.NOMBRE CUENTANOMBRE,
  DETALLE_COMPROBANTE_CNT.TERCERO,
  DETALLE_COMPROBANTE_CNT.SUCURSAL, 
  TERCERO.NOMBRE TERCERONOMBRE,
  SUM(DETALLE_COMPROBANTE_CNT.VALOR_DEBITO) SUMADEBITO,
  SUM(DETALLE_COMPROBANTE_CNT.VALOR_CREDITO) SUMACREDITO,
  SUM(DETALLE_COMPROBANTE_CNT.BASE_GRAVABLE) AS SUMABASE
FROM DETALLE_COMPROBANTE_CNT 
  INNER JOIN COMPROBANTE_CNT 
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA   = COMPROBANTE_CNT.COMPANIA 
    AND DETALLE_COMPROBANTE_CNT.ANO        = COMPROBANTE_CNT.ANO 
    AND DETALLE_COMPROBANTE_CNT.TIPO_CPTE  = COMPROBANTE_CNT.TIPO 
    AND DETALLE_COMPROBANTE_CNT.COMPROBANTE = COMPROBANTE_CNT.NUMERO 
  INNER JOIN PLAN_CONTABLE
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA     = PLAN_CONTABLE.COMPANIA 
    AND DETALLE_COMPROBANTE_CNT.ANO          = PLAN_CONTABLE.ANO 
    AND DETALLE_COMPROBANTE_CNT.CUENTA       = PLAN_CONTABLE.CODIGO 
  INNER JOIN TERCERO 
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA     = TERCERO.COMPANIA 
    AND DETALLE_COMPROBANTE_CNT.TERCERO      = TERCERO.NIT
    AND DETALLE_COMPROBANTE_CNT.SUCURSAL     = TERCERO.SUCURSAL 
  INNER JOIN FUENTE_RECURSOS
    ON  DETALLE_COMPROBANTE_CNT.COMPANIA     = FUENTE_RECURSOS.COMPANIA
    AND DETALLE_COMPROBANTE_CNT.ANO          = FUENTE_RECURSOS.ANO
    AND DETALLE_COMPROBANTE_CNT.FUENTE_RECURSO     = FUENTE_RECURSOS.CODIGO    
WHERE DETALLE_COMPROBANTE_CNT.COMPANIA  = s$compania$s
  AND DETALLE_COMPROBANTE_CNT.ANO       =  s$anoTrabajo$s 
  AND DETALLE_COMPROBANTE_CNT.CUENTA       BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
  AND DETALLE_COMPROBANTE_CNT.FUENTE_RECURSO BETWEEN  's$auxiliarInicial$s' AND 's$auxiliarFinal$s'
  AND DETALLE_COMPROBANTE_CNT.FECHA    BETWEEN s$fechaInicial$s
                                       AND s$fechaFinal$s
  AND TRIM(TERCERO.NOMBRE)  BETWEEN  's$terceroInicial$s' AND 's$terceroFinal$s'
  AND PLAN_CONTABLE.MAN_AUX_FUE NOT IN 0  
GROUP BY DETALLE_COMPROBANTE_CNT.COMPANIA,
         DETALLE_COMPROBANTE_CNT.ANO,
         DETALLE_COMPROBANTE_CNT.CUENTA,
         DETALLE_COMPROBANTE_CNT.FUENTE_RECURSO,
         FUENTE_RECURSOS.NOMBRE,
         PLAN_CONTABLE.NOMBRE,
         DETALLE_COMPROBANTE_CNT.TERCERO,
         DETALLE_COMPROBANTE_CNT.SUCURSAL, 
         TERCERO.NOMBRE