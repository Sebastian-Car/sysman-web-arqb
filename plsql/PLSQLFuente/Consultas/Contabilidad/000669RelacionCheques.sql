SELECT COMPROBANTE_CNT.COMPANIA, 
       COMPROBANTE_CNT.NUMERO, 
       TO_CHAR(COMPROBANTE_CNT.FECHA, 'DD/MM/YYYY') FECHA, 
       TERCERO.NOMBRE AS NOMBRETERCERO, 
       COMPROBANTE_CNT.DESCRIPCION, 
       COMPROBANTE_CNT.TEXTO, 
       COMPROBANTE_CNT.NRO_DOCUMENTO AS NOCHEQUE, 
       COMPROBANTE_CNT.VLRAGIRAR, 
       BANCO.NOMBRE AS CUENTABANCARIA,
       TIPO_COMPROBANTE.CLASE_CONTABLE, 
       COMPROBANTE_CNT.CUENTA
FROM COMPROBANTE_CNT LEFT JOIN TERCERO ON COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA AND COMPROBANTE_CNT.TERCERO = TERCERO.NIT AND COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL 
    LEFT JOIN TIPO_COMPROBANTE ON COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA AND COMPROBANTE_CNT.TIPO = TIPO_COMPROBANTE.CODIGO 
    LEFT JOIN V_PLAN_CONTABLE BANCO ON COMPROBANTE_CNT.COMPANIA = BANCO.COMPANIA AND COMPROBANTE_CNT.ANO = BANCO.ANO AND COMPROBANTE_CNT.CUENTA = BANCO.ID
WHERE COMPROBANTE_CNT.COMPANIA        = s$compania$s 
  AND TIPO_COMPROBANTE.CLASE_CONTABLE = 'E' 
  AND COMPROBANTE_CNT.FECHA  BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY')  AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
  AND COMPROBANTE_CNT.CUENTA BETWEEN 's$bancoInicial$s' AND 's$bancoFinal$s'
