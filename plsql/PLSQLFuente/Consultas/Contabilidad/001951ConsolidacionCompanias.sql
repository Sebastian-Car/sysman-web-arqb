SELECT * 
FROM 
(
SELECT 0 ORDENADO, COMPANIA.NOMBRE COMPANIA, PLAN_CONTABLE.NOMBRE NOMBRECUENTA, PLAN_CONTABLE.ANO, PLAN_CONTABLE.CODIGO, SALDO0, SALDO1, SALDO2, SALDO3, SALDO4, SALDO5, SALDO6, SALDO7, SALDO8, SALDO9, SALDO10, SALDO11, SALDO12
FROM PLAN_CONTABLE INNER JOIN COMPANIA
  ON PLAN_CONTABLE.COMPANIA  = COMPANIA.CODIGO
WHERE COMPANIA =$P{PR_COMPANIACONSOLIDADORA}
  AND ANO =  $P{PR_ANIO}
  AND LENGTH(PLAN_CONTABLE.CODIGO)=1
UNION ALL
SELECT -1 ORDENADO, COMPANIA.NOMBRE COMPANIA, CONSO.NOMBRE NOMBRECUENTA, PLAN_CONTABLE.ANO, PLAN_CONTABLE.CODIGO, SUM(PLAN_CONTABLE.SALDO0), SUM(PLAN_CONTABLE.SALDO1), SUM(PLAN_CONTABLE.SALDO2), SUM(PLAN_CONTABLE.SALDO3), SUM(PLAN_CONTABLE.SALDO4), SUM(PLAN_CONTABLE.SALDO5), SUM(PLAN_CONTABLE.SALDO6), SUM(PLAN_CONTABLE.SALDO7), SUM(PLAN_CONTABLE.SALDO8), SUM(PLAN_CONTABLE.SALDO9), SUM(PLAN_CONTABLE.SALDO10), SUM(PLAN_CONTABLE.SALDO11), SUM(PLAN_CONTABLE.SALDO12)
FROM PLAN_CONTABLE INNER JOIN CONSOLIDADA
  ON PLAN_CONTABLE.COMPANIA  = CONSOLIDADA.NITCOMPANIA
INNER JOIN COMPANIA
  ON CONSOLIDADA.NITCOMPANIA  = COMPANIA.CODIGO
INNER JOIN (SELECT COMPANIA, ANO, CODIGO, NOMBRE
            FROM PLAN_CONTABLE
            )CONSO 
  ON CONSO.COMPANIA  = CONSOLIDADA.COMPANIACON
 AND CONSO.ANO       = PLAN_CONTABLE.ANO
 AND CONSO.CODIGO     = PLAN_CONTABLE.CODIGO
WHERE CONSOLIDADA.COMPANIACON = $P{PR_COMPANIACONSOLIDADORA}
  AND PLAN_CONTABLE.ANO = $P{PR_ANIO}
  AND LENGTH(PLAN_CONTABLE.CODIGO)=1
GROUP BY  COMPANIA.NOMBRE, CONSO.NOMBRE, PLAN_CONTABLE.ANO, PLAN_CONTABLE.CODIGO
) 
ORDER BY CODIGO, ORDENADO
