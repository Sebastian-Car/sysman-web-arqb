SELECT 
  SF_FACTURA.FECHA_EXPEDICION
 ,SF_FACTURA.NUMERO_FACTURA
 ,SF_FACTURA.OBSERVACIONES
 ,SF_FACTURA.VALOR_TOTAL
 ,SF_FACTURA.TERCERO 
 ,TERCERO.NOMBRE NOMTERCERO
FROM SF_FACTURA
  INNER JOIN TERCERO
     ON SF_FACTURA.COMPANIA = TERCERO.COMPANIA
    AND SF_FACTURA.TERCERO  = TERCERO.NIT
    AND SF_FACTURA.SUCURSAL = TERCERO.SUCURSAL
WHERE SF_FACTURA.COMPANIA     = s$compania$s
  AND SF_FACTURA.TIPO_FACTURA = s$tipoCobro$s
  AND SF_FACTURA.FECHA_EXPEDICION BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND SF_FACTURA.ANULADA         IN(0)
  AND SF_FACTURA.INTERFAZADA NOT IN(0)
  AND SF_FACTURA.FECHA_PAGO IS NULL
