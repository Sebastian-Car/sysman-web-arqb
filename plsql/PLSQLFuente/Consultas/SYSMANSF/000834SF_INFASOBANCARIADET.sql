SELECT DISTINCT SF_ASOBANCARIA_LOG.FECHARECAUDO,
  SF_ASOBANCARIA_LOG.FECHA,
  SF_ASOBANCARIA_LOG.USUARIO,
  SF_ASOBANCARIA_LOG.BANCO,
  SF_ASOBANCARIA_LOG.UBICACION_ARCHIVO,
  SF_ASOBANCARIA_PAGOS.TIPO CODIGO,
  SF_ASOBANCARIA_PAGOS.FACTURA,
  SF_ASOBANCARIA_PAGOS.PREVAL,
  SF_ASOBANCARIA_PAGOS.ID,
  SF_ASOBANCARIA_ENC_ARC.HORA
FROM SF_ASOBANCARIA_ENC_ARC 
INNER JOIN SF_ASOBANCARIA_PAGOS 
   ON SF_ASOBANCARIA_ENC_ARC.COMPANIA = SF_ASOBANCARIA_PAGOS.COMPANIA
  AND SF_ASOBANCARIA_ENC_ARC.ID       = SF_ASOBANCARIA_PAGOS.ID
INNER JOIN SF_ASOBANCARIA_LOG
   ON SF_ASOBANCARIA_PAGOS.COMPANIA   = SF_ASOBANCARIA_LOG.COMPANIA
  AND SF_ASOBANCARIA_PAGOS.ID         = SF_ASOBANCARIA_LOG.ID
WHERE SF_ASOBANCARIA_ENC_ARC.COMPANIA = s$compania$s 
 AND  CASE
    	WHEN s$indicador$s NOT IN('0')
    	THEN SF_ASOBANCARIA_LOG.FECHA
    	ELSE TO_CHAR(SF_ASOBANCARIA_LOG.FECHARECAUDO,'DD/MM/YYYY')
  	  END                       = s$fecha$s
AND SF_ASOBANCARIA_LOG.BANCO = s$banco$s
