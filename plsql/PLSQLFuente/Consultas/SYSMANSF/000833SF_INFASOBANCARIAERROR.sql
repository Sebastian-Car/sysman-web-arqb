SELECT DISTINCT SF_ASOBANCARIA_LOG.FECHARECAUDO,
  SF_ASOBANCARIA_LOG.FECHA,
  SF_ASOBANCARIA_ERROR.ID,
  SF_ASOBANCARIA_ERROR.BANCO,
  SF_ASOBANCARIA_ERROR.TIPO CODIGO,
  SF_ASOBANCARIA_ERROR.FACTURA,
  SF_ASOBANCARIA_ERROR.DESCRIPCION
FROM SF_ASOBANCARIA_ERROR
INNER JOIN SF_ASOBANCARIA_LOG
ON  SF_ASOBANCARIA_ERROR.COMPANIA = SF_ASOBANCARIA_LOG.COMPANIA
AND SF_ASOBANCARIA_ERROR.ID = SF_ASOBANCARIA_LOG.ID
WHERE SF_ASOBANCARIA_ERROR.COMPANIA = s$compania$s
  AND  CASE
		  WHEN s$indicador$s NOT IN ('0')
		  THEN SF_ASOBANCARIA_LOG.FECHA
		  ELSE TO_CHAR(SF_ASOBANCARIA_LOG.FECHARECAUDO,'DD/MM/YYYY')
		END                       = s$fecha$s
  AND SF_ASOBANCARIA_ERROR.BANCO = s$banco$s
