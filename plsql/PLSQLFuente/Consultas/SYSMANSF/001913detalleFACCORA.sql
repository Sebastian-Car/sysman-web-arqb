WITH CONSULTA AS
  (SELECT DETALLE_FACTURA_CONTRATO.CONCEPTO CONCEPTO,
    CASE
      WHEN CONCEPTOFACTURACION.CAMBIA_DESCRIPCION <> 0
      THEN DETALLE_FACTURA_CONTRATO.TEXTO_CONCEPTO
      ELSE CONCEPTOFACTURACION.NOMBRE
    END DESCRIPCION,
    NVL(DETALLE_FACTURA_CONTRATO.VALOR_INTERES_INC,0) + NVL(DETALLE_FACTURA_CONTRATO.DEUDA_INTERES_INC,0) INCO,
    DETALLE_FACTURA_CONTRATO.VALOR_BASE VALOR_BASE,
    DETALLE_FACTURA_CONTRATO.VALOR_IVA IVA,
    DETALLE_FACTURA_CONTRATO.VALOR_RETEFUENTE RTEFTE,
    DETALLE_FACTURA_CONTRATO.NUMERO_FACTURA NUMERO_FACTURA,
    DETALLE_FACTURA_CONTRATO.ANO ANO,
    DETALLE_FACTURA_CONTRATO.MES MES
  FROM DETALLE_FACTURA_CONTRATO
  INNER JOIN CONCEPTOFACTURACION
  ON DETALLE_FACTURA_CONTRATO.COMPANIA      = CONCEPTOFACTURACION.COMPANIA
  AND DETALLE_FACTURA_CONTRATO.CONCEPTO     = CONCEPTOFACTURACION.CODIGO
  WHERE DETALLE_FACTURA_CONTRATO.COMPANIA   = s$compania$s
  AND DETALLE_FACTURA_CONTRATO.ANO          = s$anio$s
  AND DETALLE_FACTURA_CONTRATO.TIPO_FACTURA = 's$tipoFactura$s'
  AND DETALLE_FACTURA_CONTRATO.CONTRATO    =  s$comprobanteInicial$s 
  )
SELECT CONSULTA.CONCEPTO,
  CONSULTA.DESCRIPCION,
  CONSULTA.VALOR_BASE + CONSULTA.INCO BASE,
  CONSULTA.IVA,
  CONSULTA.RTEFTE,
  CONSULTA.VALOR_BASE + CONSULTA.INCO + CONSULTA.IVA - CONSULTA.RTEFTE TOTALCONCEPTOFACT
FROM CONSULTA
ORDER BY CONSULTA.CONCEPTO 
