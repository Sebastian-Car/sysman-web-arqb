SELECT
	CUENTABANCOS.COMPANIA ,
	CUENTABANCOS.ANO,
	CUENTABANCOS.BANCO,
	CUENTABANCOS.CUENTANUMERO ,
	CUENTABANCOS.IDCONTABLE
FROM
	CUENTABANCOS
INNER JOIN (
	SELECT
		COMPANIA ,
		ANO,
		BANCO ,
		CUENTANUMERO,
		COUNT('x')
	FROM
		CUENTABANCOS
	WHERE
		CUENTANUMERO IS NOT NULL
	GROUP BY
		COMPANIA ,
		ANO,
		BANCO ,
		CUENTANUMERO
	HAVING
		COUNT('x')>1) VAL ON
	CUENTABANCOS.COMPANIA = VAL.COMPANIA
	AND CUENTABANCOS.ANO = VAL.ANO
	AND CUENTABANCOS.banco = VAL.BANCO
	AND CUENTABANCOS.CUENTANUMERO = VAL.CUENTANUMERO