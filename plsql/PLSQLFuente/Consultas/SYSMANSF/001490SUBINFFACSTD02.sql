SELECT SF_ABONOS.TIPO,
  SF_ABONOS.CODIGO,
  SF_ABONOS.CUOTA_INICIAL AS EFECTIVO,
  SF_ABONOS.CUOTAS,
  SUM(SF_DETALLE_ABONO.TOTAL_CUOTA)                  AS CREDITO,
  SUM(SF_DETALLE_ABONO.TOTAL_CUOTA)/DECODE(SF_ABONOS.CUOTAS,0,1,SF_ABONOS.CUOTAS) AS PROMEDIO_CUOTA
FROM SF_DETALLE_ABONO
INNER JOIN SF_ABONOS
ON SF_DETALLE_ABONO.COMPANIA      = SF_ABONOS.COMPANIA
AND SF_DETALLE_ABONO.TIPO_ABONO   = SF_ABONOS.TIPO
AND SF_DETALLE_ABONO.CODIGO_ABONO = SF_ABONOS.CODIGO
WHERE SF_ABONOS.COMPANIA          =s$compania$s
AND SF_DETALLE_ABONO.CUOTA       <>0
GROUP BY SF_ABONOS.TIPO,
  SF_ABONOS.CODIGO,
  SF_ABONOS.CUOTA_INICIAL,
  SF_ABONOS.CUOTAS
