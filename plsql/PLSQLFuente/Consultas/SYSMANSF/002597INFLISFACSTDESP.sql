MERGE INTO CONSULTAS FIN USING (SELECT '002597INFLISFACSTDESP' INFORME ,TO_CLOB(q'[SELECT TO_CHAR(SF_FACTURA.FECHA_EXPEDICION,'DD/MM/YYYY') FECHA,
  SF_FACTURA.TIPO_FACTURA,
  SF_FACTURA.NUMERO_FACTURA,
  SF_DETALLE_FACTURA.TERCERO,
  TERCERO.NOMBRE,
  SF_CONCEPTOS.CODIGO COD_CONCEPTO,
  SF_CONCEPTOS.NOMBRE CONCEPTO,
  SF_DETALLE_FACTURA.CANTIDAD,
  SF_DETALLE_FACTURA.VALOR_UNITARIO,
  SF_DETALLE_FACTURA.VALOR_BASE,
  SF_DETALLE_FACTURA.VALOR_IVA,
  CASE WHEN (NVL(NOTA_CREDITO.VALOR_CREDITO,0) = SF_FACTURA.VALOR_TOTAL) THEN SF_DETALLE_FACTURA.VALOR_NETO ELSE 0 END NOTAS_CREDITO,
  CASE WHEN (NVL(NOTA_CREDITO.VALOR_CREDITO,0) = SF_FACTURA.VALOR_TOTAL) THEN 0 ELSE SF_DETALLE_FACTURA.VALOR_NETO END VALOR_NETO
FROM SF_FACTURA
INNER JOIN SF_DETALLE_FACTURA
ON SF_FACTURA.NUMERO_FACTURA = SF_DETALLE_FACTURA.FACTURA
AND SF_FACTURA.TIPO_FACTURA  = SF_DETALLE_FACTURA.TIPO_FACTURA
AND SF_FACTURA.COMPANIA      = SF_DETALLE_FACTURA.COMPANIA
INNER JOIN TERCERO
ON SF_FACTURA.COMPANIA=TERCERO.COMPANIA
AND SF_FACTURA.TERCERO=TERCERO.NIT
AND SF_FACTURA.SUCURSAL=TERCERO.SUCURSAL
INNER JOIN SF_CONCEPTOS 
ON SF_DETALLE_FACTURA.COMPANIA=SF_CONCEPTOS.COMPANIA
AND SF_DETALLE_FACTURA.ANO=SF_CONCEPTOS.ANO
AND SF_DETALLE_FACTURA.TIPOCOBRO=SF_CONCEPTOS.TIPOCOBRO
AND SF_DETALLE_FACTURA.CONCEPTO=SF_CONCEPTOS.CODIGO
LEFT JOIN (
SELECT DCC.COMPANIA, DCC.ANO_AFECT, DCC.TIPO_CPTE_AFECT, DCC.CMPTE_AFECTADO, SUM(VALOR_CREDITO) VALOR_CREDITO 
FROM DETALLE_COMPROBANTE_CNT DCC
INNER JOIN PLAN_CONTABLE PC
ON PC.COMPANIA = DCC.COMPANIA
AND PC.ANO = DCC.ANO
AND PC.CODIGO = DCC.CUENTA
INNER JOIN TIPO_COMPROBANTE TC
ON TC.COMPANIA = DCC.COMPANIA
AND TC.CODIGO = TIPO_CPTE
WHERE DCC.COMPANIA = 's$compania$s'
AND DCC.TIPO_CPTE_AFECT = 's$tipoCobro$s'
AND PC.CLASECUENTA = 'C'
AND TC.CLASE_CONTABLE = 'N'
GROUP BY DCC.COMPANIA, DCC.ANO_AFECT, DCC.TIPO_CPTE_AFECT, DCC.CMPTE_AFECTADO) NOTA_CREDITO
ON NOTA_CREDITO.COMPANIA = SF_FACTURA.COMPANIA
AND NOTA_CREDITO.ANO_AFECT = SF_FACTURA.ANO_CPTE
AND NOTA_CREDITO.TIPO_CPTE_AFECT = SF_FACTURA.TIPO_CPTE
AND NOTA_CREDITO.CMPTE_AFECTADO = SF_FACTURA.NRO_CPTE
WHERE SF_FACTURA.COMPANIA='s$compania$s'
AND SF_FACTURA.TIPO_FACTURA= 's$tipoCobro$s'
AND SF_FACTURA.TERCERO BETWEEN 's$nitInicial$s' AND 's$nitFinal$s'
AND SF_DETALLE_FACTURA.CONCEPTO BETWEEN 's$conceptoInicial$s' AND 's$conceptoFinal$s'
AND TRUNC(SF_FACTURA.FECHA_EXPEDICION) BETWEEN TRUNC(s$fechaIncial$s) AND TRUNC(s$fechaFinal$s)
ORDER BY FECHA, NUMERO_FACTURA ]') CONSULTA, 69 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);