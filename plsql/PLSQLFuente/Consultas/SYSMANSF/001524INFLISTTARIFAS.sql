SELECT 
  SF_TARIFA.CODIGO, 
  SF_TARIFA.NOMBRE, 
  SF_TARIFA.PORCENTAJE, 
  SF_TARIFA.TOMAR_VLR,
  SF_TARIFA.ANO,
  CASE WHEN SF_TARIFA.TOMAR_VLR = 0
  THEN 'NO'
  ELSE 'SI'
  END TOMA_VLR
  FROM SF_TARIFA
  WHERE 
  SF_TARIFA.COMPANIA = s$compania$s
  AND SF_TARIFA.ANO =s$ano$s
  AND SF_TARIFA.TIPOCOBRO = 's$tipoCobro$s'
  s$condicion$s
 ORDER BY SF_TARIFA.CODIGO 
