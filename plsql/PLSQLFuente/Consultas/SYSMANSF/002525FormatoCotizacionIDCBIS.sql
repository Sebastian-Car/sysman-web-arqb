        SF_COTIZACION.NUMERO NUMERO,
        TERCERO.DIRECCION,
        CIUDAD.NOMBRE NOM_CIUDAD,
        TERCERO.NIT,
        TERCERO.TELEFONOS,
        EXTRACT (DAY FROM SF_COTIZACION.FECHA_SOLICITUD) || ' DE ' || UPPER(PCK_SYSMAN_UTL.FC_NOMBRE_MES(EXTRACT (MONTH FROM SF_COTIZACION.FECHA_SOLICITUD))) || ' DE ' || EXTRACT (YEAR FROM SF_COTIZACION.FECHA_SOLICITUD)  FECHA_SOLICITUD,
        SF_COTIZACION.FECHA_VENCIMIENTO,
