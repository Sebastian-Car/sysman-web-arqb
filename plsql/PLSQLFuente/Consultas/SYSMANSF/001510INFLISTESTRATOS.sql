SELECT  
  SF_ESTRATO.CODIGO, 
  SF_ESTRATO.NOMBRE,
  SF_ESTRATO.TARIFA,
  SF_ESTRATO.ANO,
  CASE WHEN SF_ESTRATO.TOMAR_VLR = 0
 THEN 'NO'
  ELSE 'SI'
  END TOMA_VLR
  FROM SF_ESTRATO
WHERE 
SF_ESTRATO.COMPANIA = s$compania$s
 AND SF_ESTRATO.ANO = s$ano$s 
AND SF_ESTRATO.TIPOCOBRO = 's$tipoCobro$s'
  s$condicion$s
ORDER BY SF_ESTRATO.CODIGO 
