MERGE INTO CONSULTAS FIN USING (SELECT '002650ListadoCotizacionesCaducar' INFORME ,TO_CLOB(q'[SELECT
    C.COMPANIA,
    C.TIPO,
    C.NUMERO,
    C.TERCERO,
    T.NOMBRE NOMBRETERCERO,
    TO_CHAR(C.FECHA_VENCIMIENTO, 'DD/MM/YYYY') AS FECHA,
    C.CAMBIAR_ESTADO
FROM
    SF_COTIZACION   C
    LEFT JOIN TERCERO         T ON C.COMPANIA = T.COMPANIA
                           AND C.TERCERO = T.NIT
                           AND C.SUCURSAL = T.SUCURSAL
WHERE
   C.COMPANIA = s$compania$s
    AND C.FECHA_VENCIMIENTO <= TO_DATE('s$fechaCorte$s','DD/MM/YYYY') 
    AND C.ESTADO IN (1,2)
    ORDER BY   C.COMPANIA,
    C.TIPO,
    C.NUMERO]') CONSULTA, 69 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);