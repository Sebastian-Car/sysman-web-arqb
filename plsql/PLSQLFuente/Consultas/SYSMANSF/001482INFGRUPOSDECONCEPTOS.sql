SELECT 
  SF_FACTURA.FECHA_EXPEDICION,
  SF_FACTURA.GRUPO_CONCEPTOS,
  SF_GRUPOS_CONCEPTOS.NOMBRE,
  SF_GRUPOS_CONCEPTOS.TIPOCOBRO,
  SF_TIPO_COBRO.NOMBRE NOMBRETC,
  SUM(SF_FACTURA.CANTIDAD_GRUPO) AS CANTGRUPO,
  SUM(SF_FACTURA.TOTAL_GRUPO)    AS TOTALGRUPO
FROM SF_FACTURA
INNER JOIN SF_GRUPOS_CONCEPTOS
ON SF_FACTURA.COMPANIA         = SF_GRUPOS_CONCEPTOS.COMPANIA
AND SF_FACTURA.TIPOCOBRO       = SF_GRUPOS_CONCEPTOS.TIPOCOBRO
AND SF_FACTURA.GRUPO_CONCEPTOS = SF_GRUPOS_CONCEPTOS.CODIGO
INNER JOIN SF_TIPO_COBRO
ON  SF_FACTURA.COMPANIA         = SF_TIPO_COBRO.COMPANIA
AND SF_FACTURA.ANO              = SF_TIPO_COBRO.ANO
AND SF_FACTURA.TIPO_FACTURA     = SF_TIPO_COBRO.CODIGO
WHERE SF_FACTURA.COMPANIA = s$compania$s
AND SF_FACTURA.FECHA_EXPEDICION BETWEEN s$fechaInicial$s AND s$fechaFinal$s
AND SF_FACTURA.GRUPO_CONCEPTOS BETWEEN s$grupoInicial$s AND s$grupoFinal$s
GROUP BY SF_FACTURA.FECHA_EXPEDICION,
  SF_FACTURA.GRUPO_CONCEPTOS,
  SF_GRUPOS_CONCEPTOS.NOMBRE,
  SF_TIPO_COBRO.NOMBRE,
  SF_GRUPOS_CONCEPTOS.TIPOCOBRO
