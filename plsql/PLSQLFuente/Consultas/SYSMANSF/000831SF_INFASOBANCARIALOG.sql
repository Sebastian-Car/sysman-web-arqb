SELECT DISTINCT SF_ASOBANCARIA_LOG.FECHARECAUDO,
  SF_ASOBANCARIA_LOG.FECHA,
  SF_ASOBANCARIA_LOG.BANCO,
  SF_ASOBANCARIA_LOG.USUARIO,
  SF_ASOBANCARIA_LOG.ID,
  SF_ASOBANCARIA_LOG.HORA,
  SF_ASOBANCARIA_LOG.NUMERO_PAGOS,
  SF_ASOBANCARIA_LOG.NUMERO_ERRORES,
  SF_ASOBANCARIA_LOG.UBICACION_ARCHIVO
FROM SF_ASOBANCARIA_LOG
WHERE COMPANIA = 's$compania$s'
AND CASE
     WHEN 's$indicador$s' NOT IN ('0')
     THEN SF_ASOBANCARIA_LOG.FECHA
     ELSE TO_CHAR(SF_ASOBANCARIA_LOG.FECHARECAUDO,'DD/MM/YYYY')
    END                      = s$fecha$s
AND SF_ASOBANCARIA_LOG.BANCO = s$banco$s
