MERGE INTO CONSULTAS FIN USING (SELECT '002663ContratosVencimientos' INFORME ,TO_CLOB(q'[SELECT SF_CONTRATOS.NUMERO, SF_CONTRATOS.TIPO, SF_CONTRATOS.TOTAL_TCONTRATADO, SF_CONTRATOS.FECHA_INICIO,
SF_CONTRATOS.FECHA_FINALIZACION, TRIM(SF_CONTRATOS.TERCERO) TERCERO, TERCERO.NOMBRE NOMBRETERCERO,
CASE SF_CONTRATOS.ESTADO WHEN 2 THEN 'Activo' WHEN 3 THEN 'Suspendido' WHEN 5 THEN 'Cancelado' END ESTADO,
SF_CONTRATOS.NUMCOTIZACION, SF_CONTRATOS.TIPOCOTIZACION, IMF.DENOMINACION, SF_CONCEPTOS.NOMBRE, IMF.AREA,
SF_DETALLE_CONTRATO.VALOR_UNITARIO, SF_DETALLE_CONTRATO.VALOR_IVA,
((SF_DETALLE_CONTRATO.CANTIDAD * SF_DETALLE_CONTRATO.VALOR_UNITARIO)+SF_DETALLE_CONTRATO.VALOR_IVA) TOTAL,
CASE IMF.TIPO WHEN 1 THEN 'Local'           WHEN 2 THEN 'Mezzanine'           WHEN 3 THEN 'Bodega'           WHEN 4 THEN 'Sótano'            WHEN 5 THEN 'Oficina'           WHEN 6 THEN 'Piso'           WHEN 7 THEN 'Casa'           WHEN 8 THEN 'Lote'           WHEN 9 THEN 'Plazoleta'           WHEN 10 THEN 'Galeria Bochica'           WHEN 11 THEN 'Edificio'           WHEN 12 THEN 'Terraza'           WHEN 13 THEN 'Baño'           WHEN 14 THEN 'Pasillo'           WHEN 15 THEN 'Espacio' END TIPOIMF,
CASE SF_CONTRATOS.EDIFICIO WHEN  1 THEN 'Ascensores' WHEN 2 THEN 'Pedregal' WHEN 3 THEN 'RTN' WHEN 4 THEN 'Bachue' WHEN 5 THEN 'RTS' WHEN 6 THEN 'Bochica' WHEN 7 THEN '0001' WHEN 8 THEN '0003' END EDIFICIO,
SF_CONTRATOS.PORCFONDO
, SF_CONTRATOS.TIEMPO_CONTRATADO
FROM SF_CONTRATOS
INNER JOIN TERCERO ON
(SF_CONTRATOS.COMPANIA = TERCERO.COMPANIA
AND SF_CONTRATOS.TERCERO = TERCERO.NIT)
LEFT JOIN SF_DETALLE_CONTRATO ON
(SF_CONTRATOS.COMPANIA = SF_DETALLE_CONTRATO.COMPANIA
AND SF_CONTRATOS.TIPO = SF_DETALLE_CONTRATO.TIPO
AND SF_CONTRATOS.NUMERO = SF_DETALLE_CONTRATO.NUMERO)
LEFT JOIN INMUEBLE_FACTURACION IMF ON
(IMF.COMPANIA = SF_DETALLE_CONTRATO.COMPANIA
AND IMF.CODIGOINMUEBLE = SF_DETALLE_CONTRATO.INMUEBLE)
LEFT JOIN SF_CONCEPTOS ON
(SF_DETALLE_CONTRATO.COMPANIA = SF_CONCEPTOS.COMPANIA
AND SF_DETALLE_CONTRATO.CONCEPTO = SF_CONCEPTOS.CODIGO
AND SF_CONTRATOS.TIPO = SF_CONCEPTOS.TIPOCOBRO)
WHERE SF_CONTRATOS.COMPANIA = 's$compania$s'
AND SF_CONTRATOS.FECHA_INICIO BETWEEN 's$fechaPrevia$s' AND 's$fechaCorte$s'
AND SF_CONTRATOS.ESTADO IN (2)]') CONSULTA, 69 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, 'mrosero' CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);