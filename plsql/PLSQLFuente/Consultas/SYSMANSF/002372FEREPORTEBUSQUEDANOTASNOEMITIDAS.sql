MERGE INTO CONSULTAS FIN USING (SELECT '002372FEREPORTEBUSQUEDANOTASNOEMITIDAS' INFORME ,TO_CLOB(q'[SELECT
	SF_OBJETO_COBRO.NRO_FACTURA ,
	TO_CHAR(SF_OBJETO_COBRO.FECHA_SOLICITUD,'DD/MM/YYYY')  FECHA,
	SF_OBJETO_COBRO.TERCERO,
	SF_OBJETO_COBRO.VALOR_TOTAL VALOR
FROM
	SF_OBJETO_COBRO
LEFT JOIN TERCERO ON
	SF_OBJETO_COBRO.COMPANIA = TERCERO.COMPANIA
	AND SF_OBJETO_COBRO.TERCERO = TERCERO.NIT
	AND SF_OBJETO_COBRO.SUCURSAL = TERCERO.SUCURSAL
LEFT JOIN SF_FACTURA ON
	SF_FACTURA.COMPANIA = SF_OBJETO_COBRO.COMPANIA
	AND SF_FACTURA.TIPOCOBRO = SF_OBJETO_COBRO.TIPOCOBRO
	AND SF_FACTURA.ANO = SF_OBJETO_COBRO.ANO
	AND SF_FACTURA.CODIGO_COBRO = SF_OBJETO_COBRO.CODIGO_COBRO
WHERE
	SF_OBJETO_COBRO.COMPANIA = s$compania$s
	AND SF_OBJETO_COBRO.FECHA_SOLICITUD BETWEEN s$fechaI$s AND s$fechaF$s
	AND SF_OBJETO_COBRO.TIPOCOBRO = s$tipoCobro$s
	AND SF_FACTURA.ANULADA  != '-1'
	AND 
	SF_OBJETO_COBRO.NRO_FACTURA NOT IN (
	SELECT
		ESTADOFAC.NUMERO
	FROM
		ESTADOFAC)
ORDER BY
	SF_OBJETO_COBRO.CODIGO_COBRO DESC]') CONSULTA, 69 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);