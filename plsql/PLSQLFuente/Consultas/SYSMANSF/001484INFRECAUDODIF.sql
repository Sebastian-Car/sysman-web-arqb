SELECT SF_ABONOS.CODIGO,
  SF_ABONOS.TERCERO,
  SF_ABONOS.FECHA_INICIO,
  TERCERO.NOMBRE,
  SF_ABONOS.DEUDA_TOTAL,
  SF_ABONOS.CUOTAS,
  SF_ABONOS.ESTADO,
  SF_ABONOS.NRO_FACT,
  SF_DETALLE_ABONO.CUOTA AS NUMEROC,
  SF_DETALLE_ABONO.FECHACUOTA,
  SF_DETALLE_ABONO.CAPITAL,
  SF_DETALLE_ABONO.INTERES,
  SF_DETALLE_ABONO.TOTAL_CUOTA,
  SF_DETALLE_ABONO.ESTADO AS ESTADOCUOTA,
  SF_ABONOS.TASA_INTERES,
  SF_DETALLE_ABONO.INT_FINANCIACION
FROM SF_ABONOS
INNER JOIN SF_DETALLE_ABONO
ON SF_ABONOS.CODIGO    = SF_DETALLE_ABONO.CODIGO_ABONO
AND SF_ABONOS.TIPO     = SF_DETALLE_ABONO.TIPO_ABONO
AND SF_ABONOS.COMPANIA = SF_DETALLE_ABONO.COMPANIA
LEFT JOIN TERCERO
ON SF_ABONOS.COMPANIA   = TERCERO.COMPANIA
AND SF_ABONOS.SUCURSAL  = TERCERO.SUCURSAL
AND SF_ABONOS.TERCERO   = TERCERO.NIT
WHERE SF_ABONOS.COMPANIA='s$compania$s'
AND TRUNC(SF_ABONOS.FECHA_INICIO) BETWEEN TRUNC(s$fechaInicial$s) AND TRUNC(s$fechaFinal$s)
AND SF_ABONOS.NRO_FACT BETWEEN s$facturaIni$s AND s$facturaFin$s
