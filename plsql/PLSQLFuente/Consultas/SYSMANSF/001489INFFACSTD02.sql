SELECT SF_FACTURA.ANO,
  SF_FACTURA.TIPO_FACTURA,
  SF_FACTURA.NUMERO_FACTURA,
  SF_FACTURA.TIPOCOBRO,
  SF_FACTURA.CODIGO_COBRO,
  SF_FACTURA.TERCERO,
  SF_FACTURA.SUCURSAL,
  SF_FACTURA.FECHA_EXPEDICION,
  SF_FACTURA.FECHA_VENCIMIENTO,
  SF_FACTURA.VALOR_TOTAL,
  SF_FACTURA.OBSERVACIONES,
  SF_DETALLE_FACTURA.ANO,
  SF_DETALLE_FACTURA.CONCEPTO,
  SF_DETALLE_FACTURA.CANTIDAD,
  SF_DETALLE_FACTURA.VALOR_NETO,
  SF_DETALLE_FACTURA.FECHA_INICIO_COBRO,
  SF_DETALLE_FACTURA.FECHA_FIN_COBRO,
  SF_CONCEPTOS.NOMBRE AS NOMBRECONCEPTO,
  SUBSTR(SF_CONCEPTOS.UNIDAD,0,5) AS UNIDADCONCEPTO,
  SF_DETALLE_FACTURA.VALOR_BASE,
  SF_DETALLE_FACTURA.VALOR_IVA,
  SF_DETALLE_FACTURA.VALOR_RETEFUENTE,
  SF_DETALLE_FACTURA.VALOR_DESCUENTO,
  SF_CONCEPTOS.PORCENTAJEIVA * 100 AS PIVA,
  SF_DETALLE_FACTURA.VALOR_UNITARIO,
  DECODE(SF_FACTURA.DIFERIDA,-1,'SI',1,'SI','NO') DIFERIDA,
  SF_FACTURA.TIPO_ABONO,
  SF_FACTURA.NRO_ABONO,
  SF_FACTURA.NIT_TERCEROFACTURADO,
  SF_FACTURA.NOMBRE_TERCEROFACTURADO,
  SF_FACTURA.FECHA_PAGO,
  SF_TIPO_COBRO.TIPOFACTURA_RECAUDO,
  DECODE(SF_FACTURA.ANULADA,-1,'SI',1,'SI','NO') ANULADA,
  SUBSTR(CHR(205)
  || CHR(102)
  || CHR(40)
  ||'415'
  || CHR(41)
  || NVL(PCK_SYSMAN_UTL.FC_PAR(UN_COMPANIA=>'s$compania$s',UN_NOMBRE=>'SF CODIGO EAN',UN_MODULO=>PCK_DATOS.FC_MODULOFACTURACIONGENERAL(),UN_FECHA_PAR=>SYSDATE,UN_IND_MAYUS=>0),'0')
  ||CHR(40)
  || '8020'
  ||CHR(41)
  || LPAD(SF_TIPO_COBRO.TIPOFACTURA_RECAUDO, 5, '0')
  || LPAD(SF_FACTURA.NUMERO_FACTURA, 19, '0')
  || CHR(40)
  || '3900'
  || CHR(41)
  || LPAD(SF_DETALLE_FACTURA.VALOR_NETO,14,'0')
  || CHR(40)
  || '96'
  || CHR(41)
  || TO_CHAR(SF_FACTURA.FECHA_VENCIMIENTO,'YYYYMMDD'),3 )AS PIEBARRA,
  TO_CHAR(PCK_CODIGODEBARRAS.FC_IMPRIMIRCODIGODEBARRAS(CHR(205)
  || CHR(102)
  || '415'
  || PCK_SYSMAN_UTL.FC_PAR(UN_COMPANIA=>'s$compania$s' ,UN_NOMBRE=>'SF CODIGO EAN',UN_MODULO=>PCK_DATOS.FC_MODULOFACTURACIONGENERAL(),UN_FECHA_PAR=>SYSDATE,UN_IND_MAYUS=>0)
  || '8020'
  || LPAD(SF_TIPO_COBRO.TIPOFACTURA_RECAUDO, 5, '0')
  || LPAD(SF_FACTURA.NUMERO_FACTURA, 19, '0')
  || CHR(102)
  || '3900'
  || LPAD(SF_DETALLE_FACTURA.VALOR_NETO,14,'0')
  || CHR(102)
  || '96'
  || TO_CHAR(SF_FACTURA.FECHA_VENCIMIENTO,'YYYYMMDD')))AS CODIGODEBARRAS_BIN,
  TERCERO.DIRECCION,
  CIUDAD.NOMBRE
FROM SF_DETALLE_FACTURA
INNER JOIN SF_FACTURA
ON SF_DETALLE_FACTURA.FACTURA       = SF_FACTURA.NUMERO_FACTURA
AND SF_DETALLE_FACTURA.TIPO_FACTURA = SF_FACTURA.TIPO_FACTURA
AND SF_DETALLE_FACTURA.COMPANIA     = SF_FACTURA.COMPANIA
INNER JOIN SF_CONCEPTOS
ON SF_DETALLE_FACTURA.TIPOCOBRO = SF_CONCEPTOS.TIPOCOBRO
AND SF_DETALLE_FACTURA.ANO      = SF_CONCEPTOS.ANO
AND SF_DETALLE_FACTURA.CONCEPTO = SF_CONCEPTOS.CODIGO
AND SF_DETALLE_FACTURA.COMPANIA = SF_CONCEPTOS.COMPANIA
INNER JOIN SF_TIPO_COBRO
ON SF_DETALLE_FACTURA.TIPO_FACTURA = SF_TIPO_COBRO.CODIGO
AND SF_DETALLE_FACTURA.ANO         = SF_TIPO_COBRO.ANO
AND SF_DETALLE_FACTURA.COMPANIA    = SF_TIPO_COBRO.COMPANIA
INNER JOIN TERCERO
ON SF_FACTURA.COMPANIA  =TERCERO.COMPANIA
AND SF_FACTURA.TERCERO  =TERCERO.NIT
AND SF_FACTURA.SUCURSAL =TERCERO.SUCURSAL
INNER JOIN CIUDAD
ON TERCERO.PAIS               =CIUDAD.PAIS
AND TERCERO.DEPARTAMENTO      =CIUDAD.DEPARTAMENTO
AND TERCERO.CIUDAD            =CIUDAD.CODIGO
WHERE SF_FACTURA.COMPANIA          =s$compania$s
AND SF_FACTURA.TIPO_FACTURA        ='s$tipoFactura$s'
AND SF_FACTURA.NUMERO_FACTURA      = s$noFactura$s 
AND SF_FACTURA.ANO                 = s$ano$s
AND SF_FACTURA.FECHA_PAGO IS NULL
AND SF_FACTURA.ANULADA    IN 0
OR SF_FACTURA.ANULADA     IS NULL
