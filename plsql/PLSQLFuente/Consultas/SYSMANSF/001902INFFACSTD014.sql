SELECT
	SF_FACTURA.TIPO_FACTURA,
	SF_FACTURA.NUMERO_FACTURA,
	SF_FACTURA.TIPOCOBRO,
	SF_FACTURA.CODIGO_COBRO,
	SF_FACTURA.TERCERO,
	SF_FACTURA.SUCURSAL,
	SF_FACTURA.FECHA_EXPEDICION,
	SF_FACTURA.FECHA_VENCIMIENTO,
	SF_FACTURA.VALOR_TOTAL,
	SF_FACTURA.OBSERVACIONES,
	SF_FACTURA.FECHA_PAGO,
	SF_FACTURA.NOMBRE_TERCEROFACTURADO,
	SF_FACTURA.NIT_TERCEROFACTURADO,
	SF_FACTURA.CUENTA_RECAUDO,
	SF_DETALLE_FACTURA.ANO,
	SF_DETALLE_FACTURA.CONCEPTO,
    SF_CONCEPTOS.NOMBRE NOMBRECONCEPTO,
	SF_DETALLE_FACTURA.CANTIDAD,
	SF_DETALLE_FACTURA.VALOR_NETO,
	SF_DETALLE_FACTURA.FECHA_INICIO_COBRO,
	SF_DETALLE_FACTURA.FECHA_FIN_COBRO,
	SF_DETALLE_FACTURA.VALOR_UNITARIO,
	NVL(SF_FACTURA.ANULADA,0)  EXPR1,
	SF_TIPO_COBRO.TIPOFACTURA_RECAUDO,
    SF_TIPO_COBRO.NOMBRE NOMBREFACTURA,
	SF_TIPO_COBRO.LEYENDA_FACTURA,
	SF_OBJETO_COBRO.BASEORIGINAL,
	SF_TIPO_COBRO.BANCO_CUENTA1,
	SF_TIPO_COBRO.BANCO_CUENTA2,
	SF_TIPO_COBRO.BANCO_CUENTA3,
	SF_TIPO_COBRO.BANCO_CUENTA4,
	SF_OBJETO_COBRO.FECHA_PRELIQUIDACION,
    SUBSTR(CHR(205)
        || CHR(102)
        || CHR(40)
        || '415'
        || CHR(41)
        || 's$codigoEan$s'
        || CHR(40)
        || '8020'
        || CHR(41)
        || LPAD(NVL(SF_TIPO_COBRO.TIPOFACTURA_RECAUDO,'0'),5,0)
        || LPAD(SF_FACTURA.NUMERO_FACTURA,19,0)
        || CHR(40)
        || '3900'
        || CHR(41)
        || LPAD(SF_FACTURA.VALOR_TOTAL,14,0)
        || CHR(40)
        || '96'
        || CHR(41)
        || TO_CHAR(SF_FACTURA.FECHA_VENCIMIENTO,'YYYYMMDD'),3) AS PIEBARRA,
    TO_CHAR(PCK_CODIGODEBARRAS.FC_IMPRIMIRCODIGODEBARRAS(CHR(205)
        || CHR(102)
        || '415'
        ||'s$codigoEan$s'
        || '8020'
        || LPAD(NVL(SF_TIPO_COBRO.TIPOFACTURA_RECAUDO,'0'),5,0)
        || LPAD(SF_FACTURA.NUMERO_FACTURA,19,0)
        || CHR(102)
        || '3900'
        || LPAD(SF_FACTURA.VALOR_TOTAL,14,0)
        || CHR(102)
        || '96'
        || TO_CHAR(SF_FACTURA.FECHA_VENCIMIENTO,'YYYYMMDD')))  CODIGODEBARRAS_BIN
FROM SF_OBJETO_COBRO
    INNER JOIN SF_FACTURA 
        ON SF_OBJETO_COBRO.COMPANIA =SF_FACTURA.COMPANIA 
        AND SF_OBJETO_COBRO.TIPOCOBRO =SF_FACTURA.TIPOCOBRO
        AND SF_OBJETO_COBRO.CODIGO_COBRO =SF_FACTURA.CODIGO_COBRO
    INNER JOIN SF_DETALLE_FACTURA
        ON SF_DETALLE_FACTURA.FACTURA =SF_FACTURA.NUMERO_FACTURA 
        AND SF_DETALLE_FACTURA.TIPO_FACTURA =SF_FACTURA.TIPO_FACTURA 
        AND SF_DETALLE_FACTURA.COMPANIA =SF_FACTURA.COMPANIA
        INNER JOIN SF_CONCEPTOS 
        ON SF_CONCEPTOS.COMPANIA   = SF_DETALLE_FACTURA.COMPANIA
        AND SF_CONCEPTOS.ANO       = SF_DETALLE_FACTURA.ANO
        AND SF_CONCEPTOS.TIPOCOBRO = SF_DETALLE_FACTURA.TIPOCOBRO
        AND SF_CONCEPTOS.CODIGO    = SF_DETALLE_FACTURA.CONCEPTO 
    INNER JOIN SF_TIPO_COBRO 
        ON SF_DETALLE_FACTURA.COMPANIA      = SF_TIPO_COBRO.COMPANIA 
        AND SF_DETALLE_FACTURA.ANO          = SF_TIPO_COBRO.ANO
        AND SF_DETALLE_FACTURA.TIPO_FACTURA = SF_TIPO_COBRO.CODIGO
WHERE SF_FACTURA.COMPANIA       =  s$compania$s

  AND SF_FACTURA.TIPO_FACTURA   = 's$tipoFactura$s'
  AND SF_FACTURA.NUMERO_FACTURA BETWEEN s$facturaInicial$s AND s$facturaFinal$s
