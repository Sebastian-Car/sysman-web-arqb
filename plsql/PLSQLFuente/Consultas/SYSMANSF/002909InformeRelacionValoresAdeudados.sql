  SF_FACTURA.COMPANIA,  
  SF_FACTURA.TIPO_FACTURA,
  SF_FACTURA.NUMERO_FACTURA,
  SF_FACTURA.VALOR_TOTAL,
  TERCERO.NOMBRE NOMBRETERCERO,
  SF_FACTURA.TERCERO,  
  TERCERO.DIRECCION,
  CIUDAD.NOMBRE CIUDAD,
  SF_FACTURA.OBSERVACIONES,
  TO_DATE(TO_CHAR(SF_FACTURA.DATE_CREATED,'DD/MM/YYYY'),'DD/MM/YYYY') FECHA_FACTURA,
  TO_DATE(TO_CHAR(SF_FACTURA.FECHA_VENCIMIENTO,'DD/MM/YYYY'),'DD/MM/YYYY') FECHA_VENCIMIENTO,
  TERCERO.TELEFONOS
