AND SF_FACTURA.TERCERO = TERCERO.NIT
AND SF_FACTURA.SUCURSAL = TERCERO.SUCURSAL
INNER JOIN SF_OBJETO_COBRO
ON SF_FACTURA.COMPANIA = SF_OBJETO_COBRO.COMPANIA
AND SF_FACTURA.ANO = SF_OBJETO_COBRO.ANO
AND SF_FACTURA.TIPO_FACTURA = SF_OBJETO_COBRO.TIPOCOBRO
