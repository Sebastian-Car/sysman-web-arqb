AND SF_FACTURA.TERCERO = SF_OBJETO_COBRO.TERCERO
AND SF_FACTURA.SUCURSAL = SF_OBJETO_COBRO.SUCURSAL
LEFT JOIN CIUDAD
ON TERCERO.PAIS = CIUDAD.PAIS
AND TERCERO.DEPARTAMENTO = CIUDAD.DEPARTAMENTO
AND TERCERO.CIUDAD = CIUDAD.CODIGO
WHERE SF_FACTURA.COMPANIA    = 's$compania$s'
AND NUMERO_FACTURA  NOT IN (SELECT CMPTE_AFECTADO FROM DETALLE_COMPROBANTE_CNT
WHERE COMPANIA= SF_FACTURA.COMPANIA
AND ANO_AFECT = SF_FACTURA.ANO
AND TIPO_CPTE_AFECT = SF_FACTURA.TIPO_FACTURA
AND CMPTE_AFECTADO = SF_FACTURA.NUMERO_FACTURA)
AND SF_FACTURA.FECHA_VENCIMIENTO BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
