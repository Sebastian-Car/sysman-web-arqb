SELECT F.TIPO_FACTURA,
  SF.ANO,
  F.NUMERO_FACTURA,
  F.TIPOCOBRO,
  SF.NOMBRE,
  F.CODIGO_COBRO,
  F.TERCERO,
  F.SUCURSAL,
  F.FECHA_EXPEDICION,
  F.FECHA_VENCIMIENTO,
  F.VALOR_TOTAL,
  F.OBSERVACIONES,
  NVL(ANULADA,0) AS ESTADOFACTURA,
  F.FECHA_PAGO,
  F.NOMBRE_TERCEROFACTURADO,
  F.NIT_TERCEROFACTURADO,
  F.CUENTA_RECAUDO,
  CASE WHEN D.CUOTA IN (0) 
    THEN 'INICIAL' 
    ELSE TO_CHAR(D.CUOTA) 
  END AS CUOTAD,
  D.CAPITAL,
  D.INT_FINANCIACION,
  D.TOTAL_CUOTA,
  F.COMPANIA,
  D.FECHACUOTA,
  SF.BANCO_CUENTA1,
  SF.BANCO_CUENTA2,
  SF.BANCO_CUENTA3,
  SF.BANCO_CUENTA4,
  TO_CHAR(FECHACUOTA, 'YYYY') AS ANOCUOTA,
  SF.TIPOFACTURA_RECAUDO,
  D.CODIGO_ABONO,
  D.CUOTA,
  CASE WHEN PCK_SYSMAN_UTL.FC_PAR(F.COMPANIA,'SF CODIGO EAN POR CADA TIPO DE COBRO', 1, SYSDATE, -1) = 'SI' THEN SF.CODIGOEAN ELSE 's$codigoEan$s' END CODIGOEAN,
     SUBSTR(CHR(205)
        || CHR(102)
        || CHR(40)
        || '415'
        || CHR(41)
        ||   CASE WHEN PCK_SYSMAN_UTL.FC_PAR(F.COMPANIA,'SF CODIGO EAN POR CADA TIPO DE COBRO', 1, SYSDATE, -1) = 'SI' THEN SF.CODIGOEAN ELSE 's$codigoEan$s' END
        || CHR(40)
        || '8020'
        || CHR(41)
        || LPAD(NVL(SF.TIPOFACTURA_RECAUDO,'0'),5,0)
        || LPAD(F.NUMERO_FACTURA,19,0)
        || CHR(40)
        || '3900'
        || CHR(41)
        || LPAD( D.TOTAL_CUOTA,14,0)
        || CHR(40)
        || '96'
        || CHR(41)
        || TO_CHAR(D.FECHACUOTA,'YYYYMMDD'),3) AS PIEBARRA,
    TO_CHAR(PCK_CODIGODEBARRAS.FC_IMPRIMIRCODIGODEBARRAS(CHR(205)
        || CHR(102)
        || '415'
        ||   CASE WHEN PCK_SYSMAN_UTL.FC_PAR(F.COMPANIA,'SF CODIGO EAN POR CADA TIPO DE COBRO', 1, SYSDATE, -1) = 'SI' THEN SF.CODIGOEAN ELSE 's$codigoEan$s' END
        || '8020'
        || LPAD(NVL(SF.TIPOFACTURA_RECAUDO,'0'),5,0)
        || LPAD(F.NUMERO_FACTURA,19,0)
        || CHR(102)
        || '3900'
        || LPAD( D.TOTAL_CUOTA,14,0)
        || CHR(102)
        || '96'
        || TO_CHAR(D.FECHACUOTA,'YYYYMMDD')))  CODIGODEBARRAS_BIN      
FROM SF_FACTURA     F
INNER JOIN SF_ABONOS  A
ON F.TIPO_ABONO = A.TIPO
AND F.NRO_ABONO = A.CODIGO
AND F.COMPANIA  = A.COMPANIA
INNER JOIN SF_DETALLE_ABONO  D
ON A.CODIGO    = D.CODIGO_ABONO
AND A.TIPO     = D.TIPO_ABONO
AND A.COMPANIA = D.COMPANIA
INNER JOIN SF_TIPO_COBRO SF
ON F.TIPOCOBRO         = SF.CODIGO
AND F.COMPANIA         = SF.COMPANIA
WHERE F.COMPANIA       = s$compania$s
AND  F.NUMERO_FACTURA  = s$numero$s
AND  F.TIPO_FACTURA    = 's$tipoCobro$s'
AND  F.TERCERO         = s$tercero$s
AND  D.CUOTA           = s$cuota$s
AND  SF.ANO            = s$ano$s
AND  NVL(ANULADA,0)  IN (0)