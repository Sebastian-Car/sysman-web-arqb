SF_COTIZACION.NUMERO,
TO_CHAR(SF_COTIZACION.FECHA_SOLICITUD, 'DD-Mon-YYYY') FECHA,
SF_COTIZACION.TERCERO,
TERCERO.NOMBRE NOMBRETER,
INMUEBLE_FACTURACION.DENOMINACION AS INMUEBLE,
CASE INMUEBLE_FACTURACION.EDIFICIO WHEN 1 THEN 'Ascensores'
              WHEN 2 THEN 'Pedregal'
              WHEN 3 THEN 'RTN'
              WHEN 4 THEN 'Bachue'
              WHEN 5 THEN 'RTS'
              WHEN 6 THEN 'Bochica'
              WHEN 7 THEN '001'
              WHEN 8 THEN '003'
              END EDIFICIO,
SUM(CASE WHEN SF_CONCEPTOS.TIPODECONCEPTO IN ('CN') THEN SF_DETALLE_COTIZACION.CANTIDAD ELSE 0 END) AREA,
SUM(CASE WHEN SF_CONCEPTOS.TIPODECONCEPTO IN ('CN') THEN SF_CONCEPTOS.VALOR_BASE ELSE 0 END) VALOR_AREA,
SF_COTIZACION.PORC_FONDO,
SUM(CASE WHEN SF_CONCEPTOS.TIPODECONCEPTO IN ('TA') THEN SF_CONCEPTOS.VALOR_BASE ELSE 0 END) TARIFA_AGUA,
SUM(CASE WHEN SF_CONCEPTOS.TIPODECONCEPTO IN ('TE') THEN SF_CONCEPTOS.VALOR_BASE ELSE 0 END) TARIFA_ENERGIA,
TIEMPO_COTIZADO
FROM SF_COTIZACION
LEFT JOIN TERCERO
 ON SF_COTIZACION.COMPANIA = TERCERO.COMPANIA
AND SF_COTIZACION.TERCERO = TERCERO.NIT
AND SF_COTIZACION.SUCURSAL = TERCERO.SUCURSAL
INNER JOIN SF_DETALLE_COTIZACION
 ON SF_COTIZACION.COMPANIA = SF_DETALLE_COTIZACION.COMPANIA
AND SF_COTIZACION.TIPO = SF_DETALLE_COTIZACION.TIPO
AND SF_COTIZACION.NUMERO = SF_DETALLE_COTIZACION.NUMERO
INNER JOIN INMUEBLE_FACTURACION
ON SF_DETALLE_COTIZACION.COMPANIA = INMUEBLE_FACTURACION.COMPANIA
AND SF_DETALLE_COTIZACION.INMUEBLE = INMUEBLE_FACTURACION.CODIGOINMUEBLE
AND SF_DETALLE_COTIZACION.TIPO_INMUEBLE = INMUEBLE_FACTURACION.TIPO
INNER JOIN SF_CONCEPTOS
 ON SF_DETALLE_COTIZACION.COMPANIA = SF_CONCEPTOS.COMPANIA
AND SF_DETALLE_COTIZACION.CONCEPTO = SF_CONCEPTOS.CODIGO
AND s$anio$s = SF_CONCEPTOS.ANO
 AND SF_CONCEPTOS.TIPODECONCEPTO IS NOT NULL
AND 's$tipocobro$s' = SF_CONCEPTOS.TIPOCOBRO
WHERE SF_COTIZACION.COMPANIA = 's$compania$s'
AND SF_COTIZACION.TIPO = 's$tipo$s'
AND SF_COTIZACION.NUMERO = s$numero$s
GROUP BY SF_COTIZACION.NUMERO, TO_CHAR(SF_COTIZACION.FECHA_SOLICITUD, 'DD-Mon-YYYY'), SF_COTIZACION.TERCERO, TERCERO.NOMBRE, INMUEBLE_FACTURACION.DENOMINACION,
SF_DETALLE_COTIZACION.CANTIDAD,
SF_CONCEPTOS.TIPODECONCEPTO,
CASE INMUEBLE_FACTURACION.EDIFICIO WHEN 1 THEN 'Ascensores' WHEN 2 THEN 'Pedregal' WHEN 3 THEN 'RTN' WHEN 4 THEN 'Bachue' WHEN 5 THEN 'RTS' WHEN 6 THEN 'Bochica' WHEN 7 THEN '001' WHEN 8 THEN '003' END, SF_COTIZACION.PORC_FONDO, TIEMPO_COTIZADO)
