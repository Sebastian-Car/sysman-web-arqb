SELECT SF_CONTRATOS.TIPO
  || ' - '
  ||SF_CONTRATOS.NUMERO CONTRATO_FACTURADO,
  SF_FACTURA.TERCERO,
  SF_FACTURA.NOMBRE_TERCEROFACTURADO,
  SF_CONTRATOS.TOTAL_TCONTRATADO TOTAL_CONTRATADO,
  SUM(SF_DETALLE_FACTURA.VALOR_NETO) TOTAL_FACTURADO,
  SF_CONTRATOS.TOTAL_TCONTRATADO-SUM(VALOR_NETO) SALDO_CONTRATO,
  SF_FACTURA.TIPO_FACTURA
  || ' - '
  || SF_FACTURA.NUMERO_FACTURA FACTURA,
  TO_CHAR(SF_FACTURA.FECHA_EXPEDICION,'DD/MM/YYYY')FECHA_EXPEDICION,
  TO_CHAR(SF_FACTURA.FECHA_VENCIMIENTO,'DD/MM/YYYY')FECHA_VENCIMIENTO,
  SUM(SF_DETALLE_FACTURA.VALOR_BASE) VLR_BASE,
  SUM(SF_DETALLE_FACTURA.VALOR_DESCUENTO) VLR_DESCUENTO,
  SUM(SF_DETALLE_FACTURA.VALOR_IVA) VLR_IVA,
  SUM(SF_DETALLE_FACTURA.VALOR_NETO) VLR_NETO,
  SF_FACTURA.BANCO_PAGO,
  TO_CHAR(SF_FACTURA.FECHA_PAGO,'DD/MM/YYYY')FECHA_PAGO
FROM SF_DETALLE_FACTURA
INNER JOIN SF_FACTURA
ON SF_DETALLE_FACTURA.COMPANIA      = SF_FACTURA.COMPANIA
AND SF_DETALLE_FACTURA.TIPO_FACTURA = SF_FACTURA.TIPO_FACTURA
AND SF_DETALLE_FACTURA.FACTURA      = SF_FACTURA.NUMERO_FACTURA
INNER JOIN SF_OBJETO_COBRO
ON SF_FACTURA.COMPANIA      = SF_OBJETO_COBRO.COMPANIA
AND SF_FACTURA.ANO           = SF_OBJETO_COBRO.ANO
AND SF_FACTURA.TIPOCOBRO    = SF_OBJETO_COBRO.TIPOCOBRO
AND SF_FACTURA.CODIGO_COBRO = SF_OBJETO_COBRO.CODIGO_COBRO
AND SF_FACTURA.TERCERO       = SF_OBJETO_COBRO.TERCERO
INNER JOIN SF_CONTRATOS
ON SF_OBJETO_COBRO.COMPANIA      = SF_CONTRATOS.COMPANIA
AND SF_OBJETO_COBRO.TIPOCONTRATO = SF_CONTRATOS.TIPO
AND SF_OBJETO_COBRO.CONTRATO     = SF_CONTRATOS.NUMERO
AND SF_OBJETO_COBRO.TERCERO       = SF_CONTRATOS.TERCERO
WHERE SF_CONTRATOS.COMPANIA  = 's$compania$s'
AND SF_FACTURA.FECHA_EXPEDICION BETWEEN s$fechaInicial$s AND s$fechaFinal$s
s$condicionFormato$s
AND SF_OBJETO_COBRO.TIPOCOBRO   = 's$tipoCobro$s'
AND SF_CONTRATOS.TIPO BETWEEN TO_CHAR('s$tipoInicial$s') AND TO_CHAR('s$tipoFinal$s')
AND (SF_CONTRATOS.TIPO LIKE's$tipoInicial$s'
AND SF_CONTRATOS.NUMERO >= s$numeroInicial$s)
OR (SF_CONTRATOS.TIPO LIKE's$tipoFinal$s'
AND SF_CONTRATOS.NUMERO <= s$numeroFinal$s)
AND SF_CONTRATOS.TERCERO BETWEEN 's$terceroInicial$s' AND 's$terceroFinal$s'
GROUP BY SF_CONTRATOS.TIPO,
  SF_CONTRATOS.NUMERO,
  SF_FACTURA.TERCERO,
  SF_FACTURA.NOMBRE_TERCEROFACTURADO,
  SF_CONTRATOS.TOTAL_TCONTRATADO,
  SF_FACTURA.TIPO_FACTURA,
  SF_FACTURA.FECHA_EXPEDICION,
  SF_FACTURA.FECHA_VENCIMIENTO,
  SF_FACTURA.BANCO_PAGO,
  SF_FACTURA.FECHA_PAGO,
  SF_FACTURA.TIPO_FACTURA
  || ' - '
  || SF_FACTURA.NUMERO_FACTURA 
ORDER BY SF_FACTURA.FECHA_EXPEDICION
