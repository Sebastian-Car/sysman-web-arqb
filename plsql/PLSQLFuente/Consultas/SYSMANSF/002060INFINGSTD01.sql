SELECT
     COMPROBANTE_CNT.COMPANIA,
     COMPROBANTE_CNT.ANO,
     COMPROBANTE_CNT.TIPO,
     COMPROBANTE_CNT.NUMERO,
     COMPROBANTE_CNT.FECHA,
	 COMPROBANTE_CNT.HORA,
     COMPROBANTE_CNT.DESCRIPCION,
     COMPROBANTE_CNT.TERCERO,
     TERCERO.NOMBRE            TERCERO_NOM,
     COMPROBANTE_CNT.SUCURSAL,
     DETALLE_COMPROBANTE_CNT.CONSECUTIVO,
     DETALLE_COMPROBANTE_CNT.CUENTA,
     DETALLE_COMPROBANTE_CNT.VALOR_DEBITO,
     DETALLE_COMPROBANTE_CNT.VALOR_CREDITO,
     PLAN_CONTABLE.NOMBRE,
     COMPROBANTE_CNT.VLR_BASE,
     COMPROBANTE_CNT.VLR_DOCUMENTO,
     TIPO_COMPROBANTE.NOMBRE   NOMBRECPTE
 FROM
     COMPROBANTE_CNT
     INNER JOIN DETALLE_COMPROBANTE_CNT ON COMPROBANTE_CNT.NUMERO = DETALLE_COMPROBANTE_CNT.COMPROBANTE
                                           AND COMPROBANTE_CNT.TIPO = DETALLE_COMPROBANTE_CNT.TIPO_CPTE
                                           AND COMPROBANTE_CNT.ANO = DETALLE_COMPROBANTE_CNT.ANO
                                           AND COMPROBANTE_CNT.COMPANIA = DETALLE_COMPROBANTE_CNT.COMPANIA
     INNER JOIN TERCERO ON COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA
                           AND COMPROBANTE_CNT.TERCERO = TERCERO.NIT
                           AND COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL
     INNER JOIN PLAN_CONTABLE ON DETALLE_COMPROBANTE_CNT.COMPANIA = PLAN_CONTABLE.COMPANIA
                                 AND DETALLE_COMPROBANTE_CNT.ANO = PLAN_CONTABLE.ANO
                                 AND DETALLE_COMPROBANTE_CNT.CUENTA = PLAN_CONTABLE.CODIGO
     INNER JOIN TIPO_COMPROBANTE ON COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA
                                    AND COMPROBANTE_CNT.TIPO = TIPO_COMPROBANTE.CODIGO
WHERE
    COMPROBANTE_CNT.COMPANIA = 's$compania$s'
    AND COMPROBANTE_CNT.ANO = PCK_SYSMAN_UTL.FC_ANIO(SYSDATE)
    AND COMPROBANTE_CNT.TIPO = 's$tipoComprobante$s'
    AND COMPROBANTE_CNT.NUMERO BETWEEN 's$comprobanteInicial$s' AND 's$comprobanteFinal$s'