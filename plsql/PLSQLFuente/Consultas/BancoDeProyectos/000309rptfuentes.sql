SELECT CODIGOFUENTE AS FCODIGOFUENTE,
  NOMBRE            AS FNOMBRE,
  DESCRIPCION       AS FDESCRIPCION,
  SIGLA             AS FSIGLA,
  CODIGODNP         AS FCODIGODNP,
  (CASE WHEN ORDEN = 'M' THEN 'MUNICIPAL'
  WHEN ORDEN = 'N' THEN 'NACIONAL' 
  WHEN ORDEN = 'D' THEN 'DEPARTAMENTAL'
  END
  ) FORDEN
FROM FUENTESFINANCIACION
WHERE COMPANIA = s$compania$s
AND CODIGOFUENTE  BETWEEN 's$fuenteInicial$s' AND 's$fuenteFinal$s' ORDER BY FNOMBRE
