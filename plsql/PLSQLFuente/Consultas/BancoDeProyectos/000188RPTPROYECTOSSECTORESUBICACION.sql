SELECT PROYECTOS.COMPANIA, 
  	PROYECTOS.CODIGO CODIGOPROYECTO,
	PROYECTOS.CODIGOBPIM, 
	PROYECTOS.OBJETO OBJETOPROYECTO, 
	PROYECTOS.VALORSOLICITADO, 
	DEPENDENCIA.NOMBRE NOMBRESECRETARIA, 
	BP_PROYECTOSRUBROS.DIMENSION DIMENSION_ID, 
	PROYECTOS.CODIGO||BP_PROYECTOSRUBROS.DIMENSION||BP_PROYECTOSRUBROS.SECTOR||BP_PROYECTOSRUBROS.PROGRAMA||BP_PROYECTOSRUBROS.SUBPROGRAMA AGRUP,
	PLAN_PRESUPUESTAL.NOMBRE NOMBREDIMENSION, 
	BP_PROYECTOSRUBROS.SECTOR SECTOR_ID, 
	PLAN_PRESUPUESTAL_1.NOMBRE NOMBRESECTOR, 
	BP_PROYECTOSRUBROS.PROGRAMA PROGRAMA_ID, 
	PLAN_PRESUPUESTAL_2.NOMBRE NOMBREPROGRAMA, 
	BP_PROYECTOSRUBROS.SUBPROGRAMA SUBPROGRAMA_ID, 
	PLAN_PRESUPUESTAL_3.NOMBRE NOMBRESUBPROGRAMA,
	FUENTE_RECURSOS.NOMBRE NOMBREFUENTERECURSOS, 	
  BARRIOS.NOMBRE NOMBREBARRIO
FROM PROYECTOS 
	INNER JOIN DEPENDENCIA 
		ON PROYECTOS.COMPANIA = DEPENDENCIA.COMPANIA
		AND PROYECTOS.DEPENDENCIA = DEPENDENCIA.CODIGO
  INNER JOIN PROYECTOLOCALIZACION
    	ON PROYECTOS.COMPANIA = PROYECTOLOCALIZACION.COMPANIA
    	AND PROYECTOS.CODIGO = PROYECTOLOCALIZACION.CODIGOPROYECTO
  INNER JOIN BARRIOS
    	ON PROYECTOLOCALIZACION.PAIS = BARRIOS.PAIS
    	AND PROYECTOLOCALIZACION.DEPARTAMENTO = BARRIOS.DEPARTAMENTO
    	AND PROYECTOLOCALIZACION.CIUDAD = BARRIOS.CIUDAD
    	AND PROYECTOLOCALIZACION.BARRIO = BARRIOS.CODIGO
	INNER JOIN BP_PROYECTOSRUBROS
		ON PROYECTOS.COMPANIA = BP_PROYECTOSRUBROS.COMPANIA
		AND PROYECTOS.CODIGO = BP_PROYECTOSRUBROS.PROYECTO
    AND PROYECTOS.VIGENCIAINICIO = BP_PROYECTOSRUBROS.VIGENCIA
	INNER JOIN  PLAN_PRESUPUESTAL 
		ON BP_PROYECTOSRUBROS.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
		AND BP_PROYECTOSRUBROS.DIMENSION = PLAN_PRESUPUESTAL.CODIGO
		AND BP_PROYECTOSRUBROS.VIGENCIA = PLAN_PRESUPUESTAL.ANO
	INNER JOIN PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL_1 
		ON BP_PROYECTOSRUBROS.COMPANIA = PLAN_PRESUPUESTAL_1.COMPANIA
		AND BP_PROYECTOSRUBROS.SECTOR = PLAN_PRESUPUESTAL_1.CODIGO
		AND BP_PROYECTOSRUBROS.VIGENCIA = PLAN_PRESUPUESTAL_1.ANO
	INNER JOIN  PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL_2 
		ON BP_PROYECTOSRUBROS.COMPANIA = PLAN_PRESUPUESTAL_2.COMPANIA
		AND BP_PROYECTOSRUBROS.PROGRAMA = PLAN_PRESUPUESTAL_2.CODIGO
		AND BP_PROYECTOSRUBROS.VIGENCIA = PLAN_PRESUPUESTAL_2.ANO
	INNER JOIN  PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL_3 
		ON BP_PROYECTOSRUBROS.COMPANIA = PLAN_PRESUPUESTAL_3.COMPANIA
		AND BP_PROYECTOSRUBROS.SUBPROGRAMA = PLAN_PRESUPUESTAL_3.CODIGO
		AND BP_PROYECTOSRUBROS.VIGENCIA = PLAN_PRESUPUESTAL_3.ANO
	INNER JOIN FUENTE_RECURSOS 
		ON BP_PROYECTOSRUBROS.COMPANIA = FUENTE_RECURSOS.COMPANIA
		AND BP_PROYECTOSRUBROS.FUENTERECURSOSRUBRO = FUENTE_RECURSOS.CODIGO
		AND BP_PROYECTOSRUBROS.VIGENCIA = FUENTE_RECURSOS.ANO
WHERE PROYECTOS.COMPANIA	=s$compania$s
	AND PROYECTOS.CODIGO BETWEEN 's$proyectoInicial$s' AND 's$proyectoFin$s'
	AND BP_PROYECTOSRUBROS.DIMENSION			= CASE WHEN 's$dimension$s' = 'TODOS' THEN BP_PROYECTOSRUBROS.DIMENSION ELSE 's$dimension$s' END
	AND BP_PROYECTOSRUBROS.SECTOR				= CASE WHEN 's$sector$s' = 'TODOS' THEN BP_PROYECTOSRUBROS.SECTOR ELSE 's$sector$s' END
	AND BP_PROYECTOSRUBROS.PROGRAMA				= CASE WHEN 's$programa$s' = 'TODOS' THEN BP_PROYECTOSRUBROS.PROGRAMA ELSE 's$programa$s' END
	AND BP_PROYECTOSRUBROS.SUBPROGRAMA			= CASE WHEN 's$subprograma$s' = 'TODOS' THEN BP_PROYECTOSRUBROS.SUBPROGRAMA ELSE 's$subprograma$s' END
	AND PROYECTOS.DEPENDENCIA	= CASE WHEN 's$dependencia$s' = 'TODAS' THEN PROYECTOS.DEPENDENCIA ELSE 's$dependencia$s' END
 ORDER BY PROYECTOS.CODIGO,BP_PROYECTOSRUBROS.DIMENSION,BP_PROYECTOSRUBROS.SECTOR,BP_PROYECTOSRUBROS.PROGRAMA,BP_PROYECTOSRUBROS.SUBPROGRAMA
