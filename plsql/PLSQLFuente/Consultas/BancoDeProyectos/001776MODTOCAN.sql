SELECT DISTINCT  BP_D_NOVEDADPROYECTO.SECTOR,
SECTORES.DESCRIPCION AS DESC_SECTORPROG,
 V_SOLICITUD_CDP.COMPANIA,
    V_SOLICITUD_CDP.TIPOT,
    V_SOLICITUD_CDP.CLASET,
    CASE WHEN  V_SOLICITUD_CDP.MODIFICACION IN (-1) AND  V_SOLICITUD_CDP.TIPOT IN('CDP')
         THEN  TO_NUMBER(PCK_BANCOS_PROY5.FC_SOLI_ORIGINALMONITOR1(s$compania$s,'s$tipot$s',s$consecutivo$s))
         ELSE V_SOLICITUD_CDP.CONSECUTIVO
    END CONSECUTIVO,
    V_SOLICITUD_CDP.DEPENDENCIA,
    DEPENDENCIA.NOMBRE AS NOMBREDEP,
    V_SOLICITUD_CDP.FECHA,
    TO_CHAR(V_SOLICITUD_CDP.FECHA,'DD')DIA,
    TRIM(TO_CHAR(V_SOLICITUD_CDP.FECHA,'Month'))MES,
    TO_CHAR(V_SOLICITUD_CDP.FECHA,'YYYY')ANIO,
    V_SOLICITUD_CDP.DESCRIPCION_PROGRAMA,
    V_SOLICITUD_CDP.DESCRIPCION_INDICADOR_MEDIDA DESCRIPCION_METAPRODUCTO,
    V_SOLICITUD_CDP.DESCRIPCION_METAPRODUCTO DESCRIPCION_METAPRODUCT,
    V_SOLICITUD_CDP.DESCRIPCION_METARESULTADO AS NOMBREPROYECTO,
    V_SOLICITUD_CDP.DESCRIPCION_METARESULTADO,
    V_SOLICITUD_CDP.DESCRIPCION_EJE AS DESCRIPCION_SECTOR, 
    V_SOLICITUD_CDP.CODIGOBPIM,
    V_SOLICITUD_CDP.NOMBREPROYECTO AS NOMBREPROY,
    V_SOLICITUD_CDP.DESCRIPCION_SECTOR,
    V_SOLICITUD_CDP.OBJETO,
    V_SOLICITUD_CDP.OBSERVACIONES,
    V_SOLICITUD_CDP.VIGENCIA,
    V_SOLICITUD_CDP.VALORSOLICITADO AS TOTAL_VALORSOLICITADO,
    TO_CHAR(BPTIPONOVEDAD.FECHAVERSIONDOC, 'DD/MM/YYYY') FECHAVERSIONDOC,
    BPTIPONOVEDAD.VERSIONDOC,
    BPTIPONOVEDAD.CODIGODOC,
    BPTIPONOVEDAD.TITULOFORMATO
FROM V_SOLICITUD_CDP 
	INNER JOIN BPTIPONOVEDAD
    	ON V_SOLICITUD_CDP.COMPANIA = BPTIPONOVEDAD.COMPANIA 
    	AND V_SOLICITUD_CDP.TIPOT   = BPTIPONOVEDAD.TIPOT
    	AND V_SOLICITUD_CDP.CLASET  = BPTIPONOVEDAD.CLASET
    INNER JOIN DEPENDENCIA
    	ON V_SOLICITUD_CDP.COMPANIA = DEPENDENCIA.COMPANIA 
    	AND V_SOLICITUD_CDP.DEPENDENCIA   = DEPENDENCIA.CODIGO
      INNER JOIN BP_D_NOVEDADPROYECTO
      ON V_SOLICITUD_CDP.COMPANIA = BP_D_NOVEDADPROYECTO.COMPANIA
      AND V_SOLICITUD_CDP.TIPOT = BP_D_NOVEDADPROYECTO.TIPOT
      AND V_SOLICITUD_CDP.CLASET = BP_D_NOVEDADPROYECTO.CLASET
      AND V_SOLICITUD_CDP.NOVEDAD = BP_D_NOVEDADPROYECTO.NOVEDAD
      AND V_SOLICITUD_CDP.DEPENDENCIA = BP_D_NOVEDADPROYECTO.DEPENDENCIA
      AND V_SOLICITUD_CDP.FUENTERECURSOS = BP_D_NOVEDADPROYECTO.FUENTERECURSOS
      AND V_SOLICITUD_CDP.PROYECTO = BP_D_NOVEDADPROYECTO.PROYECTO
      LEFT JOIN SECTORES
      ON BP_D_NOVEDADPROYECTO.COMPANIA = SECTORES.COMPANIA
      AND BP_D_NOVEDADPROYECTO.SECTOR = SECTORES.CODIGO
WHERE V_SOLICITUD_CDP.COMPANIA      = s$compania$s
    AND V_SOLICITUD_CDP.TIPOT       = 's$tipot$s'   
    AND V_SOLICITUD_CDP.CONSECUTIVO = s$consecutivo$s
