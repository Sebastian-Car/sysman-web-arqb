SELECT  
P.CODIGO,   P.CODIGOBPIM  CODIGO_BPIM,   P.NOMBREPROYECTO  NOMBRE_PROYECTO,   P.VIGENCIAINICIO  VIG_INICIO,    P.VIGENCIAFIN  VIG_FINAL,     P.VALORTOTAL VALOR_TOTAL,    
 P.VALORPROGRAMADO VALOR_PROGRAMADO, 
 P.VALORSOLICITADO VALOR_SOLICITADO,  
 P.VALOREJECUTADO VALOR_EJECUTADO,  
 CASE WHEN NVL(P.VALOREJECUTADO, 0) = 0 OR NVL(P.VALORPROGRAMADO, 0) = 0 THEN 0 ELSE P.VALOREJECUTADO / P.VALORPROGRAMADO END AVANCE,   D.NOMBRE  DEPENDENCIA, 
 T.NOMBRE1||' '||T.NOMBRE2||' '||T.APELLIDO1||' '||T.APELLIDO2  RESPONSABLE,    E.NOMBRE  ENTIDAD_PROPONENTE,    OBJETO,    
 FECHAREGISTRO,   FECHA_ACTUALIZACION,     TIPOREGISTRO,     
 CASE ESTADOACTUAL WHEN 'E' THEN 'Ejecución' WHEN 'S' THEN 'Sin ejecutar' WHEN 'P' THEN 'Ejecutado parcialmente' WHEN 'T' THEN 'Ejecutado' END ESTADOACTUAL 
 FROM PROYECTOS P           
  LEFT JOIN DEPENDENCIA  D 
    ON P.COMPANIA = D.COMPANIA                   
    AND P.DEPENDENCIA = D.CODIGO               
  LEFT JOIN DEPENDENCIA_RESPONSABLE  DR 
    ON P.COMPANIA = DR.COMPANIA                  
    AND P.DEPENDENCIA = DR.DEPENDENCIA                  
    AND P.RESPONSABLE = DR.RESPONSABLE               
  LEFT JOIN RESPONSABLE  R ON (DR.COMPANIA = R.COMPANIA)               
    AND (DR.RESPONSABLE = R.CEDULA)     
  LEFT JOIN TERCERO T 
    ON R.COMPANIA = T.COMPANIA         
    AND R.CEDULA = T.NIT   
    AND R.SUCURSAL = T.SUCURSAL               
  LEFT JOIN BP_ENTIDADES  E 
    ON P.COMPANIA = E.COMPANIA        
    AND P.ENTIDADPROPONENTE = E.CODIGO
 WHERE P.COMPANIA =s$compania$s
AND P.CODIGO = CASE WHEN UPPER('s$proyecto$s') = 'TODOS' THEN TO_CHAR(P.CODIGO) ELSE 's$proyecto$s' END
AND D.CODIGO = CASE WHEN UPPER('s$dependencia$s') = 'T' THEN TO_CHAR(D.CODIGO) ELSE 's$dependencia$s' END
AND P.VIGENCIAINICIO <= CASE WHEN UPPER('s$vigencia$s') = 'TODAS' THEN TO_CHAR(P.VIGENCIAINICIO) ELSE 's$vigencia$s' END
AND P.VIGENCIAFIN >= CASE WHEN UPPER('s$vigencia$s') = 'TODAS' THEN TO_CHAR(P.VIGENCIAFIN) ELSE 's$vigencia$s' END
AND P.ESTADOACTUAL = CASE WHEN UPPER('s$estadoActual$s') = 'X' THEN TO_CHAR(P.ESTADOACTUAL) ELSE 's$estadoActual$s' END
