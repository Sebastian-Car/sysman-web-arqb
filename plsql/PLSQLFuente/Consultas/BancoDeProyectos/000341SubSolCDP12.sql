SELECT DISTINCT OC.COMPANIA, 
      VOC.AUXILIAR, 
      ORDENDECOMPRA_1.CLASEORDEN, 
      ORDENDECOMPRA_1.NOMBRECONTRATISTA PRUEBA_, 
      OC.NOMBRECONTRATISTA, 
      PROYECTOS.NOMBREPROYECTO, 
      OC.CLASEORDEN, 
      OC.VALORRESERVA, 
      BPTIPONOVEDAD.CLASENOVEDAD, 
      BPNOVEDADPROYECTO.FECHA_ENVIO_BPPIM FECHABPIM, 
      BPNOVEDADPROYECTO.VOBOBP, 
      BPNOVEDADPROYECTO.ESTADO, 
      BPNOVEDADPROYECTO.CODIGO, 
      BPNOVEDADPROYECTO.NUMERORADICADO, 
      BPNOVEDADPROYECTO.OBJETO, 
      BPNOVEDADPROYECTO.TIEMPOEJECUCION, 
      BP_D_NOVEDADPROYECTO.COMPONENTE, 
      BP_D_NOVEDADPROYECTO.PROYECTO, 
      BP_D_NOVEDADPROYECTO.RUBROPRESUPUESTAL, 
      COMPONENTES.NOMBRECOMPONENTE, 
      COMPONENTES_ACTIVIDADES.NOMBREACTIVIDAD, 
      OC.VALORTOTAL, 
      V_ORDENDECOMPRA_DIS.COMPROBANTES DISPONIBILIDAD, 
      OC.VALORDISPONIBILIDAD, 
      OC.CLASEDISPONIBILIDAD, 
      V_ORDENDECOMPRA_DIS.FECHACDP FECHADISPONIBILIDAD, 
      V_ORDENDECOMPRA_DIS.FECHACDP FECHADEDISPONIBILIDAD, 
      OC.NUMERO, 
      PROYECTOS.CODIGO
FROM ORDENDECOMPRA OC
  LEFT JOIN V_ORDENDECOMPRA_AUX VOC ON
      OC.COMPANIA= VOC.COMPANIA
    AND OC.CLASEORDEN = VOC.CLASEORDEN
    AND OC.NUMERO = VOC.NUMERO
  INNER JOIN ORDENDECOMPRA ORDENDECOMPRA_1 
      ON VOC.COMPANIA = ORDENDECOMPRA_1.COMPANIA
    AND VOC.CLASEORDEN = ORDENDECOMPRA_1.CLASEORDEN
    AND VOC.NUMERO = ORDENDECOMPRA_1.NUMERO
  LEFT JOIN PROYECTOS ON 
      VOC.COMPANIA = PROYECTOS.COMPANIA 
    AND VOC.AUXILIAR = PROYECTOS.CODIGO
  LEFT JOIN V_ORDENDECOMPRA_DIS ON
      OC.COMPANIA = V_ORDENDECOMPRA_DIS.COMPANIA
    AND OC.CLASEORDEN = V_ORDENDECOMPRA_DIS.CLASEORDEN
    AND OC.NUMERO = V_ORDENDECOMPRA_DIS.NUMERO
  LEFT JOIN BP_D_NOVEDADPROYECTO ON 
      PROYECTOS.COMPANIA = BP_D_NOVEDADPROYECTO.COMPANIA  
    AND PROYECTOS.CODIGO = BP_D_NOVEDADPROYECTO.PROYECTO
  LEFT JOIN BPNOVEDADPROYECTO ON  
      BP_D_NOVEDADPROYECTO.COMPANIA = BPNOVEDADPROYECTO.COMPANIA    
    AND BP_D_NOVEDADPROYECTO.TIPOT = BPNOVEDADPROYECTO.TIPOT  
    AND BP_D_NOVEDADPROYECTO.CLASET = BPNOVEDADPROYECTO.CLASET 
    AND BP_D_NOVEDADPROYECTO.NOVEDAD = BPNOVEDADPROYECTO.CODIGO 
    AND BP_D_NOVEDADPROYECTO.DEPENDENCIA = BPNOVEDADPROYECTO.DEPENDENCIA  
  LEFT JOIN COMPONENTES_ACTIVIDADES ON  
      BP_D_NOVEDADPROYECTO.COMPANIA = COMPONENTES_ACTIVIDADES.COMPANIA  
    AND BP_D_NOVEDADPROYECTO.PROYECTO = COMPONENTES_ACTIVIDADES.CODIGOPROYECTO 
    AND BP_D_NOVEDADPROYECTO.COMPONENTE = COMPONENTES_ACTIVIDADES.COMPONENTE 
    AND BP_D_NOVEDADPROYECTO.ACTIVIDAD = COMPONENTES_ACTIVIDADES.ACTIVIDAD 
  LEFT JOIN COMPONENTES ON 
      BP_D_NOVEDADPROYECTO.COMPANIA = COMPONENTES.COMPANIA  
    AND BP_D_NOVEDADPROYECTO.PROYECTO = COMPONENTES.CODIGOPROYECTO 
    AND BP_D_NOVEDADPROYECTO.COMPONENTE = COMPONENTES.CODIGO
  LEFT JOIN BPTIPONOVEDAD ON 
      BP_D_NOVEDADPROYECTO.COMPANIA = BPTIPONOVEDAD.COMPANIA  
    AND BP_D_NOVEDADPROYECTO.TIPOT = BPTIPONOVEDAD.TIPOT 
    AND BP_D_NOVEDADPROYECTO.CLASET = BPTIPONOVEDAD.CLASET 
WHERE OC.COMPANIA = $P{PR_COMPANIA} 
AND VOC.AUXILIAR = $P{PR_PROYECTO}
AND ORDENDECOMPRA_1.CLASEORDEN='CCS' 
AND BPTIPONOVEDAD.CLASENOVEDAD = 'S' 
AND BPNOVEDADPROYECTO.VOBOBP NOT IN (0) 
AND BPNOVEDADPROYECTO.ESTADO NOT IN ('A')
ORDER BY BP_D_NOVEDADPROYECTO.PROYECTO
