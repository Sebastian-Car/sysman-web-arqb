SELECT PREGUNTAS_NOVEDAD.COMPANIA,
  PREGUNTAS_NOVEDAD.NOVEDAD,
  PREGUNTAS_NOVEDAD.PREGUNTA,
  PREGUNTAS.NOMBRE,
  PREGUNTAS_NOVEDAD.RESPUESTA
FROM PREGUNTAS_NOVEDAD
INNER JOIN PREGUNTAS
ON PREGUNTAS_NOVEDAD.COMPANIA      =PREGUNTAS.COMPANIA
AND PREGUNTAS_NOVEDAD.PREGUNTA     =PREGUNTAS.CODIGO
WHERE PREGUNTAS_NOVEDAD.COMPANIA   =  '$P!{PR_COMPANIA}'
AND PREGUNTAS_NOVEDAD.NOVEDAD      =  $P!{PR_CODIGO} 
ORDER BY PREGUNTAS_NOVEDAD.PREGUNTA