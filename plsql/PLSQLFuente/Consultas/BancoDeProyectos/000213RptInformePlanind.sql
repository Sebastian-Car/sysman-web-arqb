WITH INFORME_AVANCE AS (
SELECT P.COMPANIA, P.ID, P.VIGENCIA_INICIAL,
  SUM(M.VALOR_PROGRAMADO_META)+SUM(M.VALOR_PROGRAMADO_META_OTROS) TOTAL_PRO,
  SUM(M.VALOR_EJECUTADO_META) TOTAL_EJE,
  SUM(CASE WHEN M.VIGENCIA_META=s$nivel$s THEN VALOR_EJECUTADO_META ELSE 0 END) EJE_VIG,
  CASE WHEN SUM(M.VALOR_PROGRAMADO_META)+SUM(M.VALOR_PROGRAMADO_META_OTROS)=0 THEN 0 ELSE SUM(M.VALOR_EJECUTADO_META)/SUM(M.VALOR_PROGRAMADO_META)+SUM(M.VALOR_PROGRAMADO_META_OTROS) END CUMPLI,
  SUM(M.CANTIDAD_PROGRAMADA) C_T_PROG,
  SUM(M.CANTIDAD_EJECUTADA) C_T_EJE,
  SUM(CASE WHEN M.VIGENCIA_META=s$nivel$s THEN CANTIDAD_EJECUTADA ELSE 0 END) C_EJE_VIG,
  CASE WHEN SUM(M.CANTIDAD_PROGRAMADA)=0 THEN 0 ELSE SUM(M.CANTIDAD_EJECUTADA)/SUM(M.CANTIDAD_PROGRAMADA) END C_CUMPLI,
  (CASE WHEN SUM(M.CANTIDAD_PROGRAMADA)=0 THEN 0 ELSE SUM(M.CANTIDAD_EJECUTADA)/SUM(M.CANTIDAD_PROGRAMADA) END)*P.PONDERACION AS AVANCE_PONDER 
FROM BP_PLAN_INDICATIVO P 
  INNER JOIN BP_PLAN_INDICATIVO_METAS M ON (P.VIGENCIA_INICIAL = M.VIGENCIA_PLAN) AND (P.ID = M.ID_PLAN) AND (P.COMPANIA = M.COMPANIA)
  WHERE P.COMPANIA=s$compania$s AND 
  P.VIGENCIA_INICIAL=s$periodoGub$s
GROUP BY P.COMPANIA, P.ID, P.VIGENCIA_INICIAL, P.PONDERACION, P.AVANCE)

SELECT P.COMPANIA, P.DESCRIPCION, P.ID, P.VIGENCIA_INICIAL, P.PONDERACION AS PONDERACION, P.AVANCE, SUM(TOTAL_PRO) AS PROG,
  SUM(TOTAL_EJE) EJE, 
  SUM(EJE_VIG) EJE_VIGE, 
  SUM(CUMPLI) CUMPLIM, 
  SUM(T.C_T_PROG) C_T_PROG,
  SUM(T.C_T_EJE) C_T_EJE, 
  SUM(T.C_EJE_VIG) C_EJE_VIG, 
  SUM(T.C_CUMPLI) C_CUMPLI,
  SUM(T.AVANCE_PONDER) AVANCE_PONDER
FROM BP_PLAN_INDICATIVO P 
INNER JOIN INFORME_AVANCE T ON (P.COMPANIA = T.COMPANIA) 
      AND (P.VIGENCIA_INICIAL = T.VIGENCIA_INICIAL)
WHERE P.ID = SUBSTR(T.ID, 1,s$nivel$s)
GROUP BY P.COMPANIA, P.DESCRIPCION, P.ID, P.VIGENCIA_INICIAL,P.PONDERACION, P.AVANCE
