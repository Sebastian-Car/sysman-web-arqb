SELECT CODIGOBPIM,
  NOMBREPROYECTO,
  VALORTOTAL,
  TO_CHAR(FECHAREGISTRO,'DD/MM/YYYY') FECHAREGISTRO,
  '31/12/'
  ||VIGENCIAFIN VIGENCIAFIN,
  VALOREJECUTADO,
 NVL((VALOREJECUTADO*100)/ CASE VALORTOTAL WHEN 0 THEN 1 END ,0) AVANCE,
  OBJETO
FROM PROYECTOS
WHERE COMPANIA = s$compania$s
AND TRUNC(FECHAREGISTRO) BETWEEN TRUNC(TO_DATE('s$fechaInicial$s 00:00:00','DD/MM/YYYY HH24:MI:SS')) AND TRUNC(TO_DATE('s$fechaFinal$s 00:00:00','DD/MM/YYYY HH24:MI:SS'))
AND VALORTOTAL NOT IN (0)
