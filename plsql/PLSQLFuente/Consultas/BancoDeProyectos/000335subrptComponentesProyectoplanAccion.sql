SELECT COMPONENTES.COMPANIA, 
    COMPONENTES.CODIGOPROYECTO, 
    COMPONENTES.VIGENCIA, 
    COMPONENTES.CODIGO CODIGO_COMPONENTE, 
    COMPONENTES.NOMBRECOMPONENTE, 
    MAX(COMPONENTES.VALORPROGRAMADO) VALORPROGRAMADO, 
    NVL(COMPONENTES.VALORRECURSOSPROPIOS,0) VALORRECURSOSPROPIOS1,
    NVL(COMPONENTES.VALORSGP,0) VALORSGP1, 
    NVL(COMPONENTES.VALORREGALIAS,0) VALORREGALIAS1, 
    NVL(COMPONENTES.VALORRECURSOSESPECIALES,0) VALORRECURSOSESPECIALES1, 
    NVL(COMPONENTES.VALOR_OTROS,0) VALOR_OTROS1, 
    MAX(PROGR_COMPONENTE_INF_8.SUMADEVALOR1) SUMADEVALOR1, 
    MAX(PROGR_COMPONENTE_INF_8.SUMADEVALOR2) SUMADEVALOR2, 
    MAX(PROGR_COMPONENTE_INF_8.SUMADEVALOR3) SUMADEVALOR3, 
    MAX(PROGR_COMPONENTE_INF_8.SUMADEVALOR4) SUMADEVALOR4, 
    MIN(C_EJECUTADO_COMPONENTE.VALORAPROBADO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) COND,
    MIN(EJECUTADO_PROGRAMADO.SUMADEVALOR1) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) E_SUMADEVALOR1,
    MIN(EJECUTADO_PROGRAMADO.SUMADEVALOR2) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) E_SUMADEVALOR2,
    MIN(EJECUTADO_PROGRAMADO.SUMADEVALOR3) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) E_SUMADEVALOR3,
    MIN(EJECUTADO_PROGRAMADO.SUMADEVALOR4) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) E_SUMADEVALOR4,
    COMPONENTES.ESTADOFINANCIACION, 
    SUM(COMPONENTES.VALORPORFINANCIAR) S_VALORPORFINANCIAR
FROM COMPONENTES 
	LEFT JOIN (SELECT PROGRAMACION.COMPANIA, 
          PROGRAMACION.CODIGOPROYECTO, 
          PROGRAMACION.CODIGOCOMPONENTE, 
          PROGRAMACION.TIPOCOMPONENTE, 
          PROGRAMACION.VIGENCIA, 
          PROGRAMACION.TIPOESTADO, 
          PROGRAMACION.CODIGO_REPROGRAMADO, 
          PROGRAMACION.REPROGRAMADO, 
          PROGRAMACION.PERIOCIDAD, 
          SUM(PROGRAMACION.VALOR1) SUMADEVALOR1, 
          SUM(PROGRAMACION.PORCENTAJE1) SUMADEPORCENTAJE1, 
          SUM(PROGRAMACION.VALOR2) SUMADEVALOR2, 
          SUM(PROGRAMACION.PORCENTAJE2) SUMADEPORCENTAJE2, 
          SUM(PROGRAMACION.VALOR3) SUMADEVALOR3, 
          SUM(PROGRAMACION.PORCENTAJE3) SUMADEPORCENTAJE3,
          SUM(PROGRAMACION.VALOR4) SUMADEVALOR4, 
          SUM(PROGRAMACION.PORCENTAJE4) SUMADEPORCENTAJE4, 
          SUM(PROGRAMACION.VALOR5) SUMADEVALOR5, 
          SUM(PROGRAMACION.PORCENTAJE5) SUMADEPORCENTAJE5, 
          SUM(PROGRAMACION.VALOR6) SUMADEVALOR6, 
          SUM(PROGRAMACION.PORCENTAJE6) SUMADEPORCENTAJE6, 
          SUM(PROGRAMACION.VALOR7) SUMADEVALOR7, 
          SUM(PROGRAMACION.PORCENTAJE7) SUMADEPORCENTAJE7, 
          SUM(PROGRAMACION.VALOR8) SUMADEVALOR8, 
          SUM(PROGRAMACION.PORCENTAJE8) SUMADEPORCENTAJE8, 
          SUM(PROGRAMACION.VALOR9) SUMADEVALOR9, 
          SUM(PROGRAMACION.PORCENTAJE9) SUMADEPORCENTAJE9, 
          SUM(PROGRAMACION.VALOR10) SUMADEVALOR10, 
          SUM(PROGRAMACION.PORCENTAJE10) SUMADEPORCENTAJE10, 
          SUM(PROGRAMACION.VALOR11) SUMADEVALOR11, 
          SUM(PROGRAMACION.PORCENTAJE11) SUMADEPORCENTAJE11, 
          SUM(PROGRAMACION.VALOR12) SUMADEVALOR12, 
          SUM(PROGRAMACION.PORCENTAJE12) SUMADEPORCENTAJE12, 
          SUM(PROGRAMACION.VALORTOTAL) SUMADEVALORTOTAL, 
          SUM(PROGRAMACION.CANTIDAD) SUMADECANTIDAD
          FROM PROGRAMACION
          WHERE PROGRAMACION.COMPANIA= s$compania$s
            AND PROGRAMACION.TIPOESTADO='P'
          GROUP BY PROGRAMACION.COMPANIA, PROGRAMACION.CODIGOPROYECTO, 
            PROGRAMACION.CODIGOCOMPONENTE, PROGRAMACION.TIPOCOMPONENTE, 
            PROGRAMACION.VIGENCIA, PROGRAMACION.TIPOESTADO, 
            PROGRAMACION.CODIGO_REPROGRAMADO, PROGRAMACION.REPROGRAMADO, PROGRAMACION.PERIOCIDAD)
  PROGR_COMPONENTE_INF_8 ON (COMPONENTES.COMPANIA = PROGR_COMPONENTE_INF_8.COMPANIA) 
    AND (COMPONENTES.CODIGOPROYECTO = PROGR_COMPONENTE_INF_8.CODIGOPROYECTO) 
    AND (COMPONENTES.CODIGO = PROGR_COMPONENTE_INF_8.CODIGOCOMPONENTE) 
    AND (COMPONENTES.TIPOCOMPONENTE = PROGR_COMPONENTE_INF_8.TIPOCOMPONENTE) 
    AND (COMPONENTES.VIGENCIA = PROGR_COMPONENTE_INF_8.VIGENCIA)
  LEFT JOIN (SELECT PROGRAMACION.COMPANIA, 
        PROGRAMACION.CODIGOPROYECTO, 
        PROGRAMACION.TIPOCOMPONENTE, 
        PROGRAMACION.CODIGOCOMPONENTE, 
        PROGRAMACION.VIGENCIA, 
        PROGRAMACION.TIPOESTADO, 
        SUM(PROGRAMACION.VALORTOTAL) SUMADEVALORTOTAL, 
        BPTIPONOVEDAD.AFECTAPROGRAMACION, 
        PROGRAMACION.TIPOT_QUEAPRUEBA, 
        PROGRAMACION.CLASET_QUEAPRUEBA, 
        PROGRAMACION.CODIGO_QUEAPRUEBA, 
        PROGRAMACION.DEPENDENCIA_QUEAPRUEBA, 
        FUENTE_RECURSOS.TIPO, 
        BP_D_NOVEDADPROYECTO.VALORAPROBADO
        FROM PROGRAMACION 
			LEFT JOIN BPTIPONOVEDAD ON PROGRAMACION.COMPANIA = BPTIPONOVEDAD.COMPANIA
            AND PROGRAMACION.TIPOT_QUEAPRUEBA = BPTIPONOVEDAD.TIPOT
            AND PROGRAMACION.CLASET_QUEAPRUEBA = BPTIPONOVEDAD.CLASET 
          INNER JOIN BP_D_NOVEDADPROYECTO ON PROGRAMACION.COMPANIA = BP_D_NOVEDADPROYECTO.COMPANIA
            AND PROGRAMACION.CODIGO_QUEAPRUEBA = BP_D_NOVEDADPROYECTO.NOVEDAD
            AND PROGRAMACION.CODIGOITEM_QUEAPRUEBA = BP_D_NOVEDADPROYECTO.CODIGO
            AND PROGRAMACION.TIPOT_QUEAPRUEBA = BP_D_NOVEDADPROYECTO.TIPOT
            AND PROGRAMACION.CLASET_QUEAPRUEBA = BP_D_NOVEDADPROYECTO.CLASET
            AND PROGRAMACION.DEPENDENCIA_QUEAPRUEBA = BP_D_NOVEDADPROYECTO.DEPENDENCIA
            AND PROGRAMACION.CODIGOPROYECTO = BP_D_NOVEDADPROYECTO.PROYECTO
          INNER JOIN FUENTE_RECURSOS ON (BP_D_NOVEDADPROYECTO.COMPANIA = FUENTE_RECURSOS.COMPANIA) 
          AND (BP_D_NOVEDADPROYECTO.FUENTERECURSOS = FUENTE_RECURSOS.CODIGO)
        WHERE PROGRAMACION.COMPANIA= s$compania$s
          AND PROGRAMACION.TIPOESTADO='E' 
          AND BPTIPONOVEDAD.AFECTAPROGRAMACION <> 0
        GROUP BY PROGRAMACION.COMPANIA, PROGRAMACION.CODIGOPROYECTO, PROGRAMACION.TIPOCOMPONENTE, 
          PROGRAMACION.CODIGOCOMPONENTE, PROGRAMACION.VIGENCIA, PROGRAMACION.TIPOESTADO, 
          BPTIPONOVEDAD.AFECTAPROGRAMACION, PROGRAMACION.TIPOT_QUEAPRUEBA, 
          PROGRAMACION.CLASET_QUEAPRUEBA, PROGRAMACION.CODIGO_QUEAPRUEBA, 
          PROGRAMACION.DEPENDENCIA_QUEAPRUEBA, FUENTE_RECURSOS.TIPO, 
          BP_D_NOVEDADPROYECTO.VALORAPROBADO, PROGRAMACION.CODIGOITEM_QUEAPRUEBA)
  C_EJECUTADO_COMPONENTE ON COMPONENTES.COMPANIA = C_EJECUTADO_COMPONENTE.COMPANIA
    AND COMPONENTES.CODIGO = C_EJECUTADO_COMPONENTE.CODIGOCOMPONENTE
    AND COMPONENTES.CODIGOPROYECTO = C_EJECUTADO_COMPONENTE.CODIGOPROYECTO
    AND COMPONENTES.VIGENCIA = C_EJECUTADO_COMPONENTE.VIGENCIA
  LEFT JOIN (SELECT PROGRAMACION.COMPANIA, 
          PROGRAMACION.CODIGOPROYECTO, 
          PROGRAMACION.CODIGOCOMPONENTE, 
          PROGRAMACION.TIPOCOMPONENTE, 
          PROGRAMACION.VIGENCIA, 
          PROGRAMACION.TIPOESTADO, 
          PROGRAMACION.FECHA, 
          PROGRAMACION.CODIGO_REPROGRAMADO, 
          PROGRAMACION.REPROGRAMADO, 
          PROGRAMACION.PERIOCIDAD, 
          SUM(PROGRAMACION.VALOR1) SUMADEVALOR1, 
          SUM(PROGRAMACION.PORCENTAJE1) SUMADEPORCENTAJE1, 
          SUM(PROGRAMACION.VALOR2) SUMADEVALOR2, 
          SUM(PROGRAMACION.PORCENTAJE2) SUMADEPORCENTAJE2, 
          SUM(PROGRAMACION.VALOR3) SUMADEVALOR3, 
          SUM(PROGRAMACION.PORCENTAJE3) SUMADEPORCENTAJE3, 
          SUM(PROGRAMACION.VALOR4) SUMADEVALOR4, 
          SUM(PROGRAMACION.PORCENTAJE4) SUMADEPORCENTAJE4, 
          SUM(PROGRAMACION.VALOR5) SUMADEVALOR5, 
          SUM(PROGRAMACION.PORCENTAJE5) SUMADEPORCENTAJE5, 
          SUM(PROGRAMACION.VALOR6) SUMADEVALOR6, 
          SUM(PROGRAMACION.PORCENTAJE6) SUMADEPORCENTAJE6, 
          SUM(PROGRAMACION.VALOR7) SUMADEVALOR7, 
          SUM(PROGRAMACION.PORCENTAJE7) SUMADEPORCENTAJE7, 
          SUM(PROGRAMACION.VALOR8) SUMADEVALOR8, 
          SUM(PROGRAMACION.PORCENTAJE8) SUMADEPORCENTAJE8, 
          SUM(PROGRAMACION.VALOR9) SUMADEVALOR9, 
          SUM(PROGRAMACION.PORCENTAJE9) SUMADEPORCENTAJE9, 
          SUM(PROGRAMACION.VALOR10) SUMADEVALOR10, 
          SUM(PROGRAMACION.PORCENTAJE10) SUMADEPORCENTAJE10, 
          SUM(PROGRAMACION.VALOR11) SUMADEVALOR11, 
          SUM(PROGRAMACION.PORCENTAJE11) SUMADEPORCENTAJE11, 
          SUM(PROGRAMACION.VALOR12) SUMADEVALOR12, 
          SUM(PROGRAMACION.PORCENTAJE12) SUMADEPORCENTAJE12,
		SUM(PROGRAMACION.VALORTOTAL) SUMADEVALORTOTAL, 
          SUM(PROGRAMACION.CANTIDAD) SUMADECANTIDAD
          FROM PROGRAMACION
          WHERE PROGRAMACION.COMPANIA = s$compania$s
            AND PROGRAMACION.TIPOESTADO='E'
          GROUP BY PROGRAMACION.COMPANIA, PROGRAMACION.CODIGOPROYECTO, 
            PROGRAMACION.CODIGOCOMPONENTE, PROGRAMACION.TIPOCOMPONENTE, 
            PROGRAMACION.VIGENCIA, PROGRAMACION.TIPOESTADO, PROGRAMACION.FECHA, 
            PROGRAMACION.CODIGO_REPROGRAMADO, PROGRAMACION.REPROGRAMADO, PROGRAMACION.PERIOCIDAD) 
  EJECUTADO_PROGRAMADO ON COMPONENTES.COMPANIA = EJECUTADO_PROGRAMADO.COMPANIA
    AND COMPONENTES.TIPOCOMPONENTE = EJECUTADO_PROGRAMADO.TIPOCOMPONENTE 
    AND COMPONENTES.CODIGO = EJECUTADO_PROGRAMADO.CODIGOCOMPONENTE
    AND COMPONENTES.CODIGOPROYECTO = EJECUTADO_PROGRAMADO.CODIGOPROYECTO 
    AND COMPONENTES.VIGENCIA = EJECUTADO_PROGRAMADO.VIGENCIA
WHERE COMPONENTES.COMPANIA=$P{PR_COMPANIA}
  AND COMPONENTES.CODIGOPROYECTO=$P{PR_CODIGOPROYECTO}
  AND COMPONENTES.VIGENCIA = $P{PR_VIGENCIA} 
 GROUP BY COMPONENTES.COMPANIA, 
    COMPONENTES.CODIGOPROYECTO, 
    COMPONENTES.VIGENCIA, 
    COMPONENTES.CODIGO, 
    COMPONENTES.NOMBRECOMPONENTE, 
    COMPONENTES.VALORRECURSOSPROPIOS,
    COMPONENTES.VALORSGP,
    COMPONENTES.VALORREGALIAS,
    COMPONENTES.VALORRECURSOSESPECIALES,
    COMPONENTES.VALOR_OTROS,
    COMPONENTES.ESTADOFINANCIACION
ORDER BY COMPONENTES.CODIGOPROYECTO, COMPONENTES.CODIGO
