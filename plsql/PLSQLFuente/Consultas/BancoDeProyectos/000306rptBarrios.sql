SELECT BARRIOS.CODIGO,
  BARRIOS.NOMBRE,
  TIPOSDIVISION.NOMBRE           AS TIPODIVISION,
  ASOCIACIONESMUNICIPALES.NOMBRE AS CODIGOASOCIACION
FROM BARRIOS
INNER JOIN TIPOSDIVISION
ON (TIPOSDIVISION.CODIGO = BARRIOS.TIPODIVISION)
LEFT JOIN ASOCIACIONESMUNICIPALES
ON (BARRIOS.CODIGOASOCIACION = ASOCIACIONESMUNICIPALES.CODIGO)
WHERE BARRIOS.PAIS           = 's$pais$s'
AND BARRIOS.DEPARTAMENTO     = 's$departamento$s'
AND BARRIOS.CIUDAD           = 's$ciudad$s'
ORDER BY BARRIOS.CODIGO
