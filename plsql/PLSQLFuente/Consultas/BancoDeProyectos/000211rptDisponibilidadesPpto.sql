SELECT DISTINCT
   PROYECTOS.COMPANIA
  ,PROYECTOS.CODIGO
  ,PROYECTOS.TIPOPROYECTO
  ,PROYECTOS.CODIGOBPIM
  ,PROYECTOS.NOMBREPROYECTO
  ,PROYECTOS.PROCESO
  ,PROYECTOS.ENTIDADPROPONENTE
  ,DEPENDENCIA.NOMBRE DEPENDENCIA
  ,TERCERO.NOMBRE RESPONSABLE
  ,PROYECTOS.CARGORESPONSABLE
  ,PROYECTOS.VIGENCIAINICIO
  ,PROYECTOS.VIGENCIAFIN 
FROM PROYECTOS 
  INNER JOIN DEPENDENCIA 
     ON PROYECTOS.COMPANIA = DEPENDENCIA.COMPANIA
    AND PROYECTOS.DEPENDENCIA = DEPENDENCIA.CODIGO         
  INNER JOIN BP_D_NOVEDADPROYECTO 
     ON PROYECTOS.COMPANIA = BP_D_NOVEDADPROYECTO.COMPANIA 
    AND PROYECTOS.CODIGO   = BP_D_NOVEDADPROYECTO.PROYECTO   
  INNER JOIN BPNOVEDADPROYECTO 
     ON BP_D_NOVEDADPROYECTO.COMPANIA    = BPNOVEDADPROYECTO.COMPANIA 
    AND BP_D_NOVEDADPROYECTO.CLASET      = BPNOVEDADPROYECTO.CLASET 
    AND BP_D_NOVEDADPROYECTO.TIPOT       = BPNOVEDADPROYECTO.TIPOT 
    AND BP_D_NOVEDADPROYECTO.NOVEDAD     = BPNOVEDADPROYECTO.CODIGO 
    AND BP_D_NOVEDADPROYECTO.DEPENDENCIA = BPNOVEDADPROYECTO.DEPENDENCIA 
  INNER JOIN TERCERO 
     ON PROYECTOS.COMPANIA    = TERCERO.COMPANIA
    AND PROYECTOS.RESPONSABLE = TERCERO.NIT 
    AND PROYECTOS.SUCURSAL    = TERCERO.SUCURSAL
WHERE PROYECTOS.COMPANIA = s$compania$s
  AND PROYECTOS.CODIGO BETWEEN s$proyInicial$s AND s$proyFinal$s
  AND BPNOVEDADPROYECTO.PROCESOFINANCIERO NOT IN(0)
