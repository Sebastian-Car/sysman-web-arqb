SELECT BP_PLAN_INDICATIVO.COMPANIA, 
 BP_PLAN_INDICATIVO.ID, 
 BP_PLAN_INDICATIVO.VIGENCIA_INICIAL, 
 BP_PLAN_INDICATIVO.DESCRIPCION, 
 BP_NIVEL_PLAN_IND.DESCRIPCION NIVEL, 
 SUM(BP_PLAN_INDICATIVO.PONDERACION) PONDERACION, 
 SUM(BP_PLAN_INDICATIVO.AVANCE) AVANCE, 
 SUM(BP_PLAN_INDICATIVO.AVANCE_FINANCIERO) AVANCE_FINANCIERO
 FROM BP_PLAN_INDICATIVO 
   INNER JOIN BP_NIVEL_PLAN_IND 
    ON BP_PLAN_INDICATIVO.COMPANIA = BP_NIVEL_PLAN_IND.COMPANIA
     AND BP_PLAN_INDICATIVO.VIGENCIA_INICIAL = BP_NIVEL_PLAN_IND.VIGENCIA
 WHERE LENGTH(BP_PLAN_INDICATIVO.ID)=BP_NIVEL_PLAN_IND.DIGITOS 
   AND BP_NIVEL_PLAN_IND.DIGITOS=s$nivel$s   
   AND BP_PLAN_INDICATIVO.COMPANIA=s$compania$s
   AND BP_PLAN_INDICATIVO.VIGENCIA_INICIAL= s$anio$s 
 GROUP BY BP_PLAN_INDICATIVO.COMPANIA, BP_PLAN_INDICATIVO.ID, BP_PLAN_INDICATIVO.VIGENCIA_INICIAL, BP_PLAN_INDICATIVO.DESCRIPCION, BP_NIVEL_PLAN_IND.DESCRIPCION
 ORDER BY BP_PLAN_INDICATIVO.ID
