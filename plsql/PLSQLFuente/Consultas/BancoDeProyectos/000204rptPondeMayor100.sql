SELECT BP_PLAN_INDICATIVO.COMPANIA, 
BP_PLAN_INDICATIVO.ID, 
BP_PLAN_INDICATIVO.VIGENCIA_INICIAL VIGENCIA, 
BP_PLAN_INDICATIVO.VIGENCIA_FINAL, 
BP_PLAN_INDICATIVO.DESCRIPCION, 
BP_PLAN_INDICATIVO.PONDERACION
FROM BP_PLAN_INDICATIVO
WHERE BP_PLAN_INDICATIVO.COMPANIA=s$compania$s
    AND BP_PLAN_INDICATIVO.PONDERACION>=100
    AND BP_PLAN_INDICATIVO.VIGENCIA_INICIAL=s$vigencia$s
