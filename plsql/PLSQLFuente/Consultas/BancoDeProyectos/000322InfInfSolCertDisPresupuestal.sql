SELECT DISTINCT BP_D_NOVEDADPROYECTO.COMPANIA, 
    BP_D_NOVEDADPROYECTO.NOVEDAD CODIGO1, 
    BP_D_NOVEDADPROYECTO.PROYECTO,  
    BP_D_NOVEDADPROYECTO.COMPONENTE, 
    BP_D_NOVEDADPROYECTO.ACTIVIDAD, 
    TO_CHAR(PROYECTOS.FECHAREGISTRO, 'DD/MM/YYYY') FECHAREGISTRO,
    BP_ACTIVIDADES.NOMBRE NOMBREACTIVIDAD, 
    BP_D_NOVEDADPROYECTO.ESPECIFICACION ESPECIFICACION1, 
    COMPONENTES.NOMBRECOMPONENTE, 
    BPNOVEDADPROYECTO.VALORSOLICITADO,  
    BPNOVEDADPROYECTO.TIEMPOEJECUCION, 
    BP_D_NOVEDADPROYECTO.FUENTERECURSOS, 
    BP_D_NOVEDADPROYECTO.RUBROPRESUPUESTAL,  
    BPNOVEDADPROYECTO.CLASET, 
    BP_D_NOVEDADPROYECTO.TIPOT, 
    PROYECTOS.NOMBREPROYECTO, 
    PROYECTOS.DEPENDENCIA,
    CASE BPNOVEDADPROYECTO.ESTADO WHEN 'V' THEN 'Vigente' WHEN 'A' THEN 'Anulada' WHEN 'N' THEN 'No Aprobado' WHEN 'C' THEN 'Cambio' ELSE 'Otro' END ESTADO,
    COMPONENTES.UNIDAD, 
    BP_D_NOVEDADPROYECTO.CANTIDAD, 
    PROYECTOS.CODIGOBPIM,  
    BPNOVEDADPROYECTO.AFECTADO, 
    BPNOVEDADPROYECTO.VIGENCIA, 
    PROYECTOS.VALORTOTAL VALORTOTALPROYECTO,  
    TERCERO.NOMBRE RESPONSABLENOMBRE, 
    NVL(BP_D_NOVEDADPROYECTO.ACTIVIDAD,0) ACT, 
    PROYECTOS.FINANCIADO_REGALIAS 
FROM BP_D_NOVEDADPROYECTO 
	INNER JOIN BPNOVEDADPROYECTO ON 
			BP_D_NOVEDADPROYECTO.COMPANIA 		= BPNOVEDADPROYECTO.COMPANIA
		AND BP_D_NOVEDADPROYECTO.NOVEDAD 		= BPNOVEDADPROYECTO.CODIGO
		AND BP_D_NOVEDADPROYECTO.CLASET 		= BPNOVEDADPROYECTO.CLASET
		AND BP_D_NOVEDADPROYECTO.TIPOT 			= BPNOVEDADPROYECTO.TIPOT
		AND BP_D_NOVEDADPROYECTO.DEPENDENCIA 	= BPNOVEDADPROYECTO.DEPENDENCIA
	INNER JOIN COMPONENTES ON 
			BP_D_NOVEDADPROYECTO.COMPANIA 		= COMPONENTES.COMPANIA
		AND BP_D_NOVEDADPROYECTO.PROYECTO 		= COMPONENTES.CODIGOPROYECTO
		AND BP_D_NOVEDADPROYECTO.COMPONENTE 	= COMPONENTES.CODIGO
		AND BP_D_NOVEDADPROYECTO.TIPOCOMPONENTE = COMPONENTES.TIPOCOMPONENTE 
	INNER JOIN RESPONSABLE ON 
			BPNOVEDADPROYECTO.COMPANIA 			= RESPONSABLE.COMPANIA
		AND BPNOVEDADPROYECTO.RESPONSABLE 		= RESPONSABLE.CEDULA
		AND BPNOVEDADPROYECTO.SUCURSAL  		= RESPONSABLE.SUCURSAL
	INNER JOIN TERCERO ON 
			RESPONSABLE.COMPANIA				= TERCERO.COMPANIA
		AND RESPONSABLE.CEDULA 					= TERCERO.NIT
		AND RESPONSABLE.SUCURSAL 				= TERCERO.SUCURSAL
	INNER JOIN PROYECTOS ON 
			BP_D_NOVEDADPROYECTO.COMPANIA 		= PROYECTOS.COMPANIA
		AND BP_D_NOVEDADPROYECTO.PROYECTO 		= PROYECTOS.CODIGO
	INNER JOIN BP_ACTIVIDADES ON 
			BP_D_NOVEDADPROYECTO.COMPANIA 		= BP_ACTIVIDADES.COMPANIA
		AND BP_D_NOVEDADPROYECTO.ACTIVIDAD 		= BP_ACTIVIDADES.CODIGO
WHERE BP_D_NOVEDADPROYECTO.COMPANIA				= s$compania$s 
  AND BP_D_NOVEDADPROYECTO.PROYECTO  BETWEEN 's$proyectoInicial$s' AND  's$proyectoFin$s'  
  AND BPNOVEDADPROYECTO.VIGENCIA 				= CASE WHEN 's$vigencia$s' = 'TODAS' THEN TO_CHAR(BPNOVEDADPROYECTO.VIGENCIA) ELSE 's$vigencia$s' END
  AND PROYECTOS.FINANCIADO_REGALIAS NOT IN (0) 
  AND BPNOVEDADPROYECTO.TIPOT					= 'SCD' 
ORDER BY BP_D_NOVEDADPROYECTO.PROYECTO
