SELECT RUBROS_NOVEDADCHIA.FUENTERECURSOS,  
       TOTAL_DISPONIBLE_PPTAL_PTG.NOMBREPLAN, 
       TOTAL_DISPONIBLE_PPTAL_PTG.APROPIADO,  
       RUBROS_NOVEDADCHIA.VALORSOLICITADO_AUX1, 
       TOTAL_DISPONIBLE_PPTAL_PTG.PAGOS,  
       TOTAL_DISPONIBLE_PPTAL_PTG.APROPIADO-TOTAL_DISPONIBLE_PPTAL_PTG.PAGOS AS SALDO,  
       (CASE WHEN TOTAL_DISPONIBLE_PPTAL_PTG.APROPIADO=0  
             THEN 0  
             ELSE TOTAL_DISPONIBLE_PPTAL_PTG.PAGOS/TOTAL_DISPONIBLE_PPTAL_PTG.APROPIADO  
             END) AS PORCENTAJE       
 FROM (SELECT V_SALDO_PLAN_PPTAL.COMPANIA, 
       V_PLAN_PRESUPUESTAL.ID, 
       V_PLAN_PRESUPUESTAL.NOMBRE NOMBREPLAN, 
       SUM(APROPIACION_DEBITO-APROPIACION_CREDITO+ADICION-REDUCCION+TRASLADO_DEBITO-TRASLADO_CREDITO) AS APROPIADO,        
       SUM(CASE WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D' THEN V_SALDO_PLAN_PPTAL.EJE_PPT_DEBITO-V_SALDO_PLAN_PPTAL.EJE_PPT_CREDITO  
                ELSE V_SALDO_PLAN_PPTAL.EJE_PPT_CREDITO-V_SALDO_PLAN_PPTAL.EJE_PPT_DEBITO  
                END)+V_SALDO_PLAN_PPTAL.REINTEGRO PAGOS 
 FROM BP_D_NOVEDADPROYECTO  
 INNER JOIN V_PLAN_PRESUPUESTAL  
 INNER JOIN V_SALDO_PLAN_PPTAL  
  ON V_PLAN_PRESUPUESTAL.COMPANIA = V_SALDO_PLAN_PPTAL.COMPANIA 
  AND V_PLAN_PRESUPUESTAL.ANO = V_SALDO_PLAN_PPTAL.ANO 
  AND V_PLAN_PRESUPUESTAL.ID = V_SALDO_PLAN_PPTAL.CODIGO 
  ON BP_D_NOVEDADPROYECTO.COMPANIA = V_PLAN_PRESUPUESTAL.COMPANIA 
  AND BP_D_NOVEDADPROYECTO.ANORUBRO = V_PLAN_PRESUPUESTAL.ANO 
  AND BP_D_NOVEDADPROYECTO.RUBROPRESUPUESTAL = V_PLAN_PRESUPUESTAL.ID 
 WHERE V_PLAN_PRESUPUESTAL.COMPANIA= s$compania$s 
  AND V_PLAN_PRESUPUESTAL.ANO= s$vigencia$s            
  AND V_SALDO_PLAN_PPTAL.MES<= TO_CHAR(SYSDATE, 'MM') 
  AND BP_D_NOVEDADPROYECTO.NOVEDAD= s$codigo$s           
  AND BP_D_NOVEDADPROYECTO.CLASET = 's$claset$s' 
 GROUP BY V_SALDO_PLAN_PPTAL.COMPANIA, 
V_PLAN_PRESUPUESTAL.ID, 
V_PLAN_PRESUPUESTAL.CODIGO, 
V_PLAN_PRESUPUESTAL.NOMBRE, 
BP_D_NOVEDADPROYECTO.NOVEDAD,  
CASE WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D' THEN V_SALDO_PLAN_PPTAL.EJE_PPT_DEBITO-V_SALDO_PLAN_PPTAL.EJE_PPT_CREDITO  
                ELSE V_SALDO_PLAN_PPTAL.EJE_PPT_CREDITO-V_SALDO_PLAN_PPTAL.EJE_PPT_DEBITO  
                END, 
V_SALDO_PLAN_PPTAL.REINTEGRO, 
BP_D_NOVEDADPROYECTO.CLASET) TOTAL_DISPONIBLE_PPTAL_PTG  
INNER JOIN (SELECT BP_D_NOVEDADPROYECTO.COMPANIA, 
       BP_D_NOVEDADPROYECTO.VALORSOLICITADO VALORSOLICITADO_AUX1, 
       BP_D_NOVEDADPROYECTO.FUENTERECURSOS, 
       BP_D_NOVEDADPROYECTO.RUBROPRESUPUESTAL 
 FROM BP_D_NOVEDADPROYECTO  
 INNER JOIN FUENTE_RECURSOS  
  ON BP_D_NOVEDADPROYECTO.FUENTERECURSOS = FUENTE_RECURSOS.CODIGO 
 AND  BP_D_NOVEDADPROYECTO.COMPANIA = FUENTE_RECURSOS.COMPANIA 
 WHERE BP_D_NOVEDADPROYECTO.COMPANIA= s$compania$s 
  AND BP_D_NOVEDADPROYECTO.TIPOT='s$tipot$s'  
  AND  BP_D_NOVEDADPROYECTO.CLASET='s$claset$s'  
  AND  BP_D_NOVEDADPROYECTO.NOVEDAD= s$codigo$s 
 GROUP BY BP_D_NOVEDADPROYECTO.COMPANIA, BP_D_NOVEDADPROYECTO.RUBROPRESUPUESTAL, BP_D_NOVEDADPROYECTO.FUENTERECURSOS, BP_D_NOVEDADPROYECTO.VALORSOLICITADO) RUBROS_NOVEDADCHIA 
  ON TOTAL_DISPONIBLE_PPTAL_PTG.COMPANIA = RUBROS_NOVEDADCHIA.COMPANIA 
  AND TOTAL_DISPONIBLE_PPTAL_PTG.ID = RUBROS_NOVEDADCHIA.RUBROPRESUPUESTAL
