SELECT  V_SOLICITUD_CDP.RUBRO RUBROPRESUPUESTAL,
        PLAN_PRESUPUESTAL.NOMBRE AS NOMBRE_PRESUPUESTAL,
        FUENTE_RECURSOS.NOMBRE AS FUENTE,
        V_SOLICITUD_CDP.VALORSOLICITADO
FROM V_SOLICITUD_CDP
  INNER JOIN  PLAN_PRESUPUESTAL
    ON V_SOLICITUD_CDP.COMPANIA           = PLAN_PRESUPUESTAL.COMPANIA
    AND V_SOLICITUD_CDP.ANORUBRO          = PLAN_PRESUPUESTAL.ANO
    AND V_SOLICITUD_CDP.RUBRO             = PLAN_PRESUPUESTAL.CODIGO   
    INNER JOIN FUENTE_RECURSOS
    ON V_SOLICITUD_CDP.COMPANIA         = FUENTE_RECURSOS.COMPANIA 
    AND V_SOLICITUD_CDP.VIGENCIA         = FUENTE_RECURSOS.ANO
   AND V_SOLICITUD_CDP.FUENTERECURSOS        = FUENTE_RECURSOS.CODIGO
WHERE V_SOLICITUD_CDP.COMPANIA      = '$P!{PR_COMPANIA}' 
  AND  V_SOLICITUD_CDP.TIPOT        = '$P!{PR_TIPOT}'
  AND V_SOLICITUD_CDP.CLASET        = '$P!{PR_CLASET}' 
  AND V_SOLICITUD_CDP.CONSECUTIVO   = $P!{PR_CONSECUTIVO} 
  AND V_SOLICITUD_CDP.DEPENDENCIA   = '$P!{PR_DEPENDENCIA}'