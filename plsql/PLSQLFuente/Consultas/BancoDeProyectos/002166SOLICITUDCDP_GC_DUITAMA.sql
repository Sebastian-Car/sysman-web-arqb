    CASE WHEN USUARIO.NOMBRE1 IN ('') THEN '' ELSE USUARIO.NOMBRE1 ||' ' END||
    CASE WHEN USUARIO.NOMBRE2 IN ('') THEN '' ELSE USUARIO.NOMBRE2 ||' ' END ||
    CASE WHEN USUARIO.APELLIDO1 IN ('') THEN '' ELSE USUARIO.APELLIDO1 ||' 'END ||
    CASE WHEN USUARIO.APELLIDO2 IN ('') THEN '' ELSE USUARIO.APELLIDO2  ||' ' END CARGO,
    SUM(BP_D_NOVEDADPROYECTO.VALORSOLICITADO) AS VALOR_S,
    BP_D_NOVEDADPROYECTO.VALORSOLICITADO,
    BP_D_NOVEDADPROYECTO.RUBROPRESUPUESTAL
    ||' '
    || NVL(BP_D_NOVEDADPROYECTO.FUENTERECURSOS, '  ') RUBROPRESUPUESTALES,
    BPNOVEDADPROYECTO.VALORTOTAL,
    PCK_SYSMAN_UTL.FC_VALOR_LETRAS(BPNOVEDADPROYECTO.VALORTOTAL) VALORLETRA,
    BPNOVEDADPROYECTO.TIPOT,
    DIMEN.DESCRIPCION AS DIMENSIOND,
    PROGRAMA.DESCRIPCION AS PROGRAMAD,
    META.DESCRIPCION AS METAD,
    SUBPROGRAMA.DESCRIPCION AS SUBPROGRAMAD,
    INDICADOR.DESCRIPCION AS INDICADORD,
    BP_PLAN_INDICATIVO.AVANCE,
    CASE WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR='MI' THEN
    'Incremento'END || CASE WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR='MM' THEN
    'Mantenimiento' END || CASE WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR='MR' THEN
    'Reducción' END || CASE WHEN BP_PLAN_INDICATIVO.TIPO_META_INDICADOR='MG' THEN
    'Gestión' END AS TIPOINDICADOR,
    BPNOVEDADPROYECTO.COMPANIA,
    BPNOVEDADPROYECTO.DEPENDENCIA,
    BP_D_NOVEDADPROYECTO.CANTIDAD,
    BP_D_NOVEDADPROYECTO.CANTIDAD_PLAN,
    BPNOVEDADPROYECTO.FECHA,
    BPNOVEDADPROYECTO.FECHAVENCIMIENTO,
    BPNOVEDADPROYECTO.NUMERORADICADO,
    BPNOVEDADPROYECTO.TIEMPOEJECUCION,
    BPNOVEDADPROYECTO.CODIGO CONSECUTIVO,
    BPNOVEDADPROYECTO.OBJETO,
    NVL(BPNOVEDADPROYECTO.OBSERVACIONES,'') OBSERVACIONES,
    BPNOVEDADPROYECTO.FECHA_VOBO,
    BPNOVEDADPROYECTO.VOBOBP,
    BPNOVEDADPROYECTO.FECHA_ENVIO_BPPIM,
    BPNOVEDADPROYECTO.CARGORESPONSABLE,
    BPNOVEDADPROYECTO.RESPONSABLE,
    BPTIPONOVEDAD.TITULOFORMATO,
    BPTIPONOVEDAD.CODIGODOC,
    BPTIPONOVEDAD.VERSIONDOC
,
    BPTIPONOVEDAD.FECHAVERSIONDOC,
    EXTRACT(YEAR FROM BPTIPONOVEDAD.FECHAVERSIONDOC) FECHAVERSION,
    BP_D_NOVEDADPROYECTO.TIPOT TIPOTD,
    BP_D_NOVEDADPROYECTO.CLASET,
    BP_D_NOVEDADPROYECTO.NOVEDAD,
    BP_D_NOVEDADPROYECTO.CODIGO,
    BP_D_NOVEDADPROYECTO.INDICADOR,
    BP_D_NOVEDADPROYECTO.CANTIDAD,
    BP_D_NOVEDADPROYECTO.ID_META_PRODUCTO,
    BP_D_NOVEDADPROYECTO.RUBROPRESUPUESTAL,
    SUM(BPNOVEDADPROYECTO.VALORSOLICITADO) VALOR_SNUMERO,
    PCK_SYSMAN_UTL.FC_VALOR_LETRAS(SUM(BPNOVEDADPROYECTO.VALORSOLICITADO)) VALOR_SLETRA,
    PLAN_PRESUPUESTAL.NOMBRE NOMBRECUENTA,
    PROYECTOS.CODIGOBPIM,
    PROYECTOS.NOMBREPROYECTO,
    COMPONENTES.NOMBRECOMPONENTE,
    BP_PLAN_INDICATIVO.DESCRIPCION DESCRIPCIONMETA,
    BP_PROYECTO_PLAN_INDICATIVO.DESCRIPCION_META DESCRIPCIONMETA,
    FUENTE_RECURSOS.NOMBRE NOMBREFUREC,
    BP_ACTIVIDADES.NOMBRE NOMBREBPAC,
    DEPENDENCIA.NOMBRE NOMDEPENDENCIA,
    BP_D_NOVEDADPROYECTO.ANORUBRO,
    BP_D_NOVEDADPROYECTO.CANTIDAD_PLAN CANTIDADPLAN,
    TERCERO.NOMBRE NOMBRETER,
    RESPONSABLE.NOMBRAMIENTO,
    NVL(TIPOADJUDICACION.DESCRIPCION,'') DESCRIPCION,
       TO_CHAR(BPNOVEDADPROYECTO.FECHA,'Day,','NLS_DATE_LANGUAGE=SPANISH')
    ||EXTRACT(DAY FROM BPNOVEDADPROYECTO.FECHA)
    || ' de '
    || PCK_SYSMAN_UTL.FC_NOMBRE_MES(EXTRACT(MONTH FROM BPNOVEDADPROYECTO.FECHA))
    || ' de '
    || EXTRACT(YEAR FROM BPNOVEDADPROYECTO.FECHA) FECHAF,
    NVL(PCK_SYSMAN_UTL.FC_PAR(BP_D_NOVEDADPROYECTO.COMPANIA, 'NOMBRE DE JEFE DE PRESUPUESTO', 52, SYSDATE, -1),'') AS NOMBREDEJEFEDEPRESUPUESTO
  FROM BP_D_NOVEDADPROYECTO
  INNER JOIN BPNOVEDADPROYECTO
  ON BP_D_NOVEDADPROYECTO.COMPANIA     = BPNOVEDADPROYECTO.COMPANIA
  AND BP_D_NOVEDADPROYECTO.TIPOT       = BPNOVEDADPROYECTO.TIPOT
  AND BP_D_NOVEDADPROYECTO.CLASET      = BPNOVEDADPROYECTO.CLASET
  AND BP_D_NOVEDADPROYECTO.NOVEDAD     = BPNOVEDADPROYECTO.CODIGO
  AND BP_D_NOVEDADPROYECTO.DEPENDENCIA = BPNOVEDADPROYECTO.DEPENDENCIA
  INNER JOIN BP_PLAN_INDICATIVO
  ON BP_D_NOVEDADPROYECTO.COMPANIA = BP_PLAN_INDICATIVO.COMPANIA
  AND BP_D_NOVEDADPROYECTO.ID_META_PRODUCTO = BP_PLAN_INDICATIVO.ID
  AND BP_D_NOVEDADPROYECTO.VIGENCIA_PLAN_INDICATIVO = BP_PLAN_INDICATIVO.VIGENCIA_INICIAL
 LEFT JOIN BP_PLAN_INDICATIVO DIMEN
   ON BP_D_NOVEDADPROYECTO.COMPANIA                       = DIMEN.COMPANIA 
  AND SUBSTR(BP_D_NOVEDADPROYECTO.ID_META_PRODUCTO,1,'s$sector$s') = DIMEN.ID  
  AND BP_D_NOVEDADPROYECTO.VIGENCIA_PLAN_INDICATIVO       = DIMEN.VIGENCIA_INICIAL
   LEFT JOIN BP_PLAN_INDICATIVO PROGRAMA
   ON BP_D_NOVEDADPROYECTO.COMPANIA                       = PROGRAMA.COMPANIA 
  AND SUBSTR(BP_D_NOVEDADPROYECTO.ID_META_PRODUCTO,1,'s$programa$s') = PROGRAMA.ID  
  AND BP_D_NOVEDADPROYECTO.VIGENCIA_PLAN_INDICATIVO       = PROGRAMA.VIGENCIA_INICIAL
     LEFT JOIN BP_PLAN_INDICATIVO META
   ON BP_D_NOVEDADPROYECTO.COMPANIA                       = META.COMPANIA 
  AND SUBSTR(BP_D_NOVEDADPROYECTO.ID_META_PRODUCTO,1,'s$meta$s') = META.ID  
  AND BP_D_NOVEDADPROYECTO.VIGENCIA_PLAN_INDICATIVO       = META.VIGENCIA_INICIAL
     LEFT JOIN BP_PLAN_INDICATIVO SUBPROGRAMA
   ON BP_D_NOVEDADPROYECTO.COMPANIA                       = SUBPROGRAMA.COMPANIA 
  AND SUBSTR(BP_D_NOVEDADPROYECTO.ID_META_PRODUCTO,1,'s$subprograma$s') = SUBPROGRAMA.ID  
  AND BP_D_NOVEDADPROYECTO.VIGENCIA_PLAN_INDICATIVO       = SUBPROGRAMA.VIGENCIA_INICIAL
   LEFT JOIN BP_PLAN_INDICATIVO INDICADOR
   ON BP_D_NOVEDADPROYECTO.COMPANIA                       = INDICADOR.COMPANIA 
  AND BP_D_NOVEDADPROYECTO.ID_META_PRODUCTO               = INDICADOR.ID  
  AND BP_D_NOVEDADPROYECTO.VIGENCIA_PLAN_INDICATIVO       = INDICADOR.VIGENCIA_INICIAL
  LEFT JOIN TIPOADJUDICACION
  ON BPNOVEDADPROYECTO.COMPANIA          = TIPOADJUDICACION.COMPANIA
  AND BPNOVEDADPROYECTO.MODALIDAD_SELECC = TIPOADJUDICACION.CODIGO
  LEFT JOIN BPTIPONOVEDAD
  ON BPNOVEDADPROYECTO.COMPANIA = BPTIPONOVEDAD.COMPANIA
  AND BPNOVEDADPROYECTO.TIPOT   = BPTIPONOVEDAD.TIPOT
  AND BPNOVEDADPROYECTO.CLASET  = BPTIPONOVEDAD.CLASET
  LEFT JOIN PLAN_PRESUPUESTAL
  ON BP_D_NOVEDADPROYECTO.COMPANIA           = PLAN_PRESUPUESTAL.COMPANIA
  AND BP_D_NOVEDADPROYECTO.RUBROPRESUPUESTAL = PLAN_PRESUPUESTAL.CODIGO
  AND BP_D_NOVEDADPROYECTO.ANORUBRO          = PLAN_PRESUPUESTAL.ANO
  INNER JOIN PROYECTOS
  ON BP_D_NOVEDADPROYECTO.COMPANIA  = PROYECTOS.COMPANIA
  AND BP_D_NOVEDADPROYECTO.PROYECTO = PROYECTOS.CODIGO
  LEFT JOIN BP_PROYECTO_PLAN_INDICATIVO
  ON BP_D_NOVEDADPROYECTO.TIPOCOMPONENTE            = BP_PROYECTO_PLAN_INDICATIVO.TIPOCOMPONENTE
  AND BP_D_NOVEDADPROYECTO.ACTIVIDAD                = BP_PROYECTO_PLAN_INDICATIVO.ACTIVIDAD
  AND BP_D_NOVEDADPROYECTO.COMPANIA                 = BP_PROYECTO_PLAN_INDICATIVO.COMPANIA
  AND BP_D_NOVEDADPROYECTO.ID_META_PRODUCTO         = BP_PROYECTO_PLAN_INDICATIVO.ID_PLAN_P
  AND BP_D_NOVEDADPROYECTO.VIGENCIA_PLAN_INDICATIVO = BP_PROYECTO_PLAN_INDICATIVO.VIGENCIA_PLAN_P
  AND BP_D_NOVEDADPROYECTO.ANORUBRO                 = BP_PROYECTO_PLAN_INDICATIVO.VIGENCIA_META_P
  AND BP_D_NOVEDADPROYECTO.PROYECTO                 = BP_PROYECTO_PLAN_INDICATIVO.PROYECTO
  AND BP_D_NOVEDADPROYECTO.COMPONENTE               = BP_PROYECTO_PLAN_INDICATIVO.COMPONENTE
  LEFT JOIN COMPONENTES
  ON BP_D_NOVEDADPROYECTO.TIPOCOMPONENTE = COMPONENTES.TIPOCOMPONENTE
  AND BP_D_NOVEDADPROYECTO.COMPANIA      = COMPONENTES.COMPANIA
  AND BP_D_NOVEDADPROYECTO.PROYECTO      = COMPONENTES.CODIGOPROYECTO
  AND BP_D_NOVEDADPROYECTO.COMPONENTE    = COMPONENTES.CODIGO
  INNER JOIN FUENTE_RECURSOS
  ON BP_D_NOVEDADPROYECTO.ANORUBRO        = FUENTE_RECURSOS.ANO
  AND BP_D_NOVEDADPROYECTO.FUENTERECURSOS = FUENTE_RECURSOS.CODIGO
  AND BP_D_NOVEDADPROYECTO.COMPANIA       = FUENTE_RECURSOS.COMPANIA
  LEFT JOIN BP_ACTIVIDADES
  ON BP_D_NOVEDADPROYECTO.ACTIVIDAD = BP_ACTIVIDADES.CODIGO
  AND BP_D_NOVEDADPROYECTO.COMPANIA = BP_ACTIVIDADES.COMPANIA
  LEFT JOIN DEPENDENCIA
  ON BP_D_NOVEDADPROYECTO.COMPANIA     = DEPENDENCIA.COMPANIA
  AND BP_D_NOVEDADPROYECTO.DEPENDENCIA = DEPENDENCIA.CODIGO
  INNER JOIN DEPENDENCIA_RESPONSABLE
  ON BPNOVEDADPROYECTO.COMPANIA     = DEPENDENCIA_RESPONSABLE.COMPANIA
  AND BPNOVEDADPROYECTO.DEPENDENCIA = DEPENDENCIA_RESPONSABLE.DEPENDENCIA
  AND BPNOVEDADPROYECTO.RESPONSABLE = DEPENDENCIA_RESPONSABLE.RESPONSABLE
  AND BPNOVEDADPROYECTO.SUCURSAL    = DEPENDENCIA_RESPONSABLE.SUCURSAL
  INNER JOIN RESPONSABLE
  ON DEPENDENCIA_RESPONSABLE.COMPANIA     = RESPONSABLE.COMPANIA
  AND DEPENDENCIA_RESPONSABLE.RESPONSABLE = RESPONSABLE.CEDULA
  AND DEPENDENCIA_RESPONSABLE.SUCURSAL    = RESPONSABLE.SUCURSAL
  INNER JOIN TERCERO
  ON RESPONSABLE.COMPANIA  = TERCERO.COMPANIA
  AND RESPONSABLE.CEDULA   = TERCERO.NIT
  AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
  INNER JOIN USUARIO
  ON BP_D_NOVEDADPROYECTO.CREATED_BY = USUARIO.CODIGO
  WHERE BPNOVEDADPROYECTO.COMPANIA     = s$compania$s
    AND BPNOVEDADPROYECTO.TIPOT           = 's$tipot$s'
    AND BPNOVEDADPROYECTO.CLASET          = 's$claset$s'
    AND BPNOVEDADPROYECTO.CODIGO          = 's$consecutivo$s'
  GROUP BY BP_D_NOVEDADPROYECTO.VALORSOLICITADO,
    BPNOVEDADPROYECTO.TIPOT,
    BPNOVEDADPROYECTO.FECHA,
    BPNOVEDADPROYECTO.FECHAVENCIMIENTO,
    BPNOVEDADPROYECTO.NUMERORADICADO,
    BPNOVEDADPROYECTO.TIEMPOEJECUCION,
    BPNOVEDADPROYECTO.CODIGO,
    DIMEN.DESCRIPCION,
    PROGRAMA.DESCRIPCION,
    META.DESCRIPCION,
    SUBPROGRAMA.DESCRIPCION,
    INDICADOR.DESCRIPCION,
    BPNOVEDADPROYECTO.COMPANIA,
    BPNOVEDADPROYECTO.DEPENDENCIA,
    BP_PLAN_INDICATIVO.AVANCE,
    BP_PLAN_INDICATIVO.TIPO_META_INDICADOR,
    BP_D_NOVEDADPROYECTO.CANTIDAD,
    BP_D_NOVEDADPROYECTO.CANTIDAD_PLAN,
    BPNOVEDADPROYECTO.OBJETO,
    NVL(BPNOVEDADPROYECTO.OBSERVACIONES,''),
    BPNOVEDADPROYECTO.FECHA_VOBO,
    BPNOVEDADPROYECTO.VOBOBP,
    BPNOVEDADPROYECTO.FECHA_ENVIO_BPPIM,
    BPNOVEDADPROYECTO.CARGORESPONSABLE,
    BPNOVEDADPROYECTO.RESPONSABLE,
    BPNOVEDADPROYECTO.VALORTOTAL,
    BPTIPONOVEDAD.TITULOFORMATO,
    BPTIPONOVEDAD.CODIGODOC,
    BPTIPONOVEDAD.VERSIONDOC,
    BPTIPONOVEDAD.FECHAVERSIONDOC,
    BP_D_NOVEDADPROYECTO.COMPANIA,
    BP_D_NOVEDADPROYECTO.TIPOT,
    BP_D_NOVEDADPROYECTO.CLASET,
    BP_D_NOVEDADPROYECTO.NOVEDAD,
    BP_D_NOVEDADPROYECTO.CODIGO,
    BP_D_NOVEDADPROYECTO.INDICADOR,
    BP_D_NOVEDADPROYECTO.CANTIDAD,
    BP_D_NOVEDADPROYECTO.ID_META_PRODUCTO,
    BP_D_NOVEDADPROYECTO.RUBROPRESUPUESTAL
    ||' '
    || NVL(BP_D_NOVEDADPROYECTO.FUENTERECURSOS, '  '),
    BP_D_NOVEDADPROYECTO.RUBROPRESUPUESTAL,
    PLAN_PRESUPUESTAL.NOMBRE,
    PROYECTOS.CODIGOBPIM,
    PROYECTOS.NOMBREPROYECTO,
    COMPONENTES.NOMBRECOMPONENTE,
    BP_PLAN_INDICATIVO.DESCRIPCION,
    BP_PROYECTO_PLAN_INDICATIVO.DESCRIPCION_META,
    FUENTE_RECURSOS.NOMBRE,
    BP_ACTIVIDADES.NOMBRE,
    DEPENDENCIA.NOMBRE,
    BP_D_NOVEDADPROYECTO.ANORUBRO,
    BP_D_NOVEDADPROYECTO.CANTIDAD_PLAN,
    TERCERO.NOMBRE,
    RESPONSABLE.NOMBRAMIENTO,
    TIPOADJUDICACION.DESCRIPCION,
    BPNOVEDADPROYECTO.VALORSOLICITADO,
    BP_D_NOVEDADPROYECTO.CREATED_BY,
    CASE WHEN USUARIO.NOMBRE1 IN ('') THEN ''  ELSE USUARIO.NOMBRE1 ||' ' END ||
    CASE WHEN USUARIO.NOMBRE2 IN ('') THEN '' ELSE USUARIO.NOMBRE2  ||' ' END||
    CASE WHEN USUARIO.APELLIDO1 IN ('') THEN '' ELSE USUARIO.APELLIDO1 ||' ' END||
    CASE WHEN USUARIO.APELLIDO2 IN ('') THEN '' ELSE USUARIO.APELLIDO2 ||' ' END,
    NVL(PCK_SYSMAN_UTL.FC_PAR(BP_D_NOVEDADPROYECTO.COMPANIA, 'NOMBRE DE JEFE DE PRESUPUESTO', 52, SYSDATE, -1),'')
  ORDER BY BP_D_NOVEDADPROYECTO.CODIGO