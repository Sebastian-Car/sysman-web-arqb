SELECT 
	BP_D_NOVEDADPROYECTO.COMPANIA, 
	BP_D_NOVEDADPROYECTO.PROYECTO, 
	BP_D_NOVEDADPROYECTO.TIPOT,  
	BP_D_NOVEDADPROYECTO.ESPECIFICACION, 
	BP_D_NOVEDADPROYECTO.VALORSOLICITADO NV_VALORSOLICITADO,  
	BP_D_NOVEDADPROYECTO.VALORAPROBADO NV_VALORAPROBADO, 
	BP_D_NOVEDADPROYECTO.FUENTERECURSOS,  
	BP_D_NOVEDADPROYECTO.CLASET, 
	BP_D_NOVEDADPROYECTO.NOVEDAD,  
	BPNOVEDADPROYECTO.VIGENCIA, 
	BPNOVEDADPROYECTO.FECHA,  
	PROYECTOS.CODIGOBPIM, 
	PROYECTOS.VALORPROGRAMADO, 
	PROYECTOS.NOMBREPROYECTO, 
	PROYECTOS.VALORSOLICITADO, 
	PROYECTOS.VALOREJECUTADO,   
	BP_PLAN_INDICATIVO.DESCRIPCION, 
	BP_PLAN_INDICATIVO.ID, 
	BP_PLAN_INDICATIVO.VIGENCIA_INICIAL,  s$niveles$s
	DEPENDENCIA.NOMBRE, 
	FUENTE_RECURSOS.NOMBRE NOM_FUENTE  
FROM BP_D_NOVEDADPROYECTO 
	INNER JOIN BPNOVEDADPROYECTO ON 
			BP_D_NOVEDADPROYECTO.COMPANIA					= BPNOVEDADPROYECTO.COMPANIA 
		AND BP_D_NOVEDADPROYECTO.TIPOT						= BPNOVEDADPROYECTO.TIPOT 
		AND BP_D_NOVEDADPROYECTO.CLASET						= BPNOVEDADPROYECTO.CLASET  
		AND BP_D_NOVEDADPROYECTO.NOVEDAD					= BPNOVEDADPROYECTO.CODIGO 
		AND BP_D_NOVEDADPROYECTO.DEPENDENCIA				= BPNOVEDADPROYECTO.DEPENDENCIA
	INNER JOIN PROYECTOS ON 
			BP_D_NOVEDADPROYECTO.COMPANIA					= PROYECTOS.COMPANIA 
		AND BP_D_NOVEDADPROYECTO.PROYECTO					= PROYECTOS.CODIGO
	LEFT JOIN BP_PLAN_INDICATIVO ON 
			BP_D_NOVEDADPROYECTO.COMPANIA					= BP_PLAN_INDICATIVO.COMPANIA 
		AND BP_D_NOVEDADPROYECTO.ID_META_PRODUCTO			= BP_PLAN_INDICATIVO.ID
		AND BP_D_NOVEDADPROYECTO.VIGENCIA_PLAN_INDICATIVO	= BP_PLAN_INDICATIVO.VIGENCIA_INICIAL  
	INNER JOIN DEPENDENCIA ON 
			PROYECTOS.COMPANIA								= DEPENDENCIA.COMPANIA 
		AND PROYECTOS.DEPENDENCIA							= DEPENDENCIA.CODIGO 
	LEFT JOIN FUENTE_RECURSOS ON 
			BP_D_NOVEDADPROYECTO.COMPANIA					= FUENTE_RECURSOS.COMPANIA 
		AND BP_D_NOVEDADPROYECTO.FUENTERECURSOS				= FUENTE_RECURSOS.CODIGO
		AND BP_D_NOVEDADPROYECTO.ANORUBRO					= FUENTE_RECURSOS.ANO   s$join$s  
WHERE BP_D_NOVEDADPROYECTO.COMPANIA							= s$compania$s  
	AND BP_D_NOVEDADPROYECTO.PROYECTO BETWEEN 's$proyectoInicial$s' AND 's$proyectoFin$s'  
	AND BP_D_NOVEDADPROYECTO.TIPOT							= 's$novedad$s'  
	AND BP_D_NOVEDADPROYECTO.CLASET							= 'B'  
	AND BPNOVEDADPROYECTO.ESTADO IN ('V','AP')  
	AND NVL(BPNOVEDADPROYECTO.VOBOBP,0)						<>0
