SELECT  BP_INCONSISTENCIAS_NOVE.ID_CODIGO, 
        BP_INCONSISTENCIAS_NOVE.TIPOT, 
        BP_INCONSISTENCIAS_NOVE.NOVEDAD, 
        DEPENDENCIA.NOMBRE  DEPENDENCIA, 
        BP_INCONSISTENCIAS_NOVE.CODIGO, 
        BP_INCONSISTENCIAS_NOVE.PROYECTO, 
        BP_INCONSISTENCIAS_NOVE.COMPONENTE, 
        BP_INCONSISTENCIAS_NOVE.TIPO_COMPONENTE, 
        BP_INCONSISTENCIAS_NOVE.ACTIVIDAD, 
        BP_INCONSISTENCIAS_NOVE.CANTIDAD, 
        BP_INCONSISTENCIAS_NOVE.VIGENCIA, 
        BP_INCONSISTENCIAS_NOVE.PROBLEMA 
FROM    BP_INCONSISTENCIAS_NOVE 
        LEFT JOIN DEPENDENCIA 
        ON BP_INCONSISTENCIAS_NOVE.DEPENDENCIA = DEPENDENCIA.CODIGO 
ORDER BY 
        BP_INCONSISTENCIAS_NOVE.TIPOT, 
        BP_INCONSISTENCIAS_NOVE.ID_CODIGO, 
        BP_INCONSISTENCIAS_NOVE.NOVEDAD
