WITH SOLICITUD AS (s$with$s),
SOLICITUD_DATOS AS (SELECT PROYECTOS.VALOREJECUTADO,
            TEMP_SOLICITUD.COMPANIA, 
            TEMP_SOLICITUD.ID_META_PRODUCTO,
            BP_PLAN_INDICATIVO.DESCRIPCION DESCRIPCION_METAPRODUCTO,
            TEMP_SOLICITUD.EJE,
            BP_PLAN_INDICATIVO_1.DESCRIPCION DESCRIPCION_EJE, 
            TEMP_SOLICITUD.SECTOR, 
            BP_PLAN_INDICATIVO_4.DESCRIPCION DESCRIPCION_SECTOR, 
            TEMP_SOLICITUD.PROGRAMA, 
            BP_PLAN_INDICATIVO_2.DESCRIPCION DESCRIPCION_PROGRAMA, 
            BP_PLAN_INDICATIVO_3.DESCRIPCION META_RESULTADO, 
            TEMP_SOLICITUD.METARESULTADO, 
            BP_PLAN_INDICATIVO_1.VIGENCIA_INICIAL, 
            BP_PLAN_INDICATIVO.VIGENCIA_FINAL,
            CASE WHEN NVL(PROYECTOS.VALOREJECUTADO, 0) = 0 THEN 0 ELSE
                      PROYECTOS.VALOREJECUTADO/PROYECTOS.VALORPROGRAMADO END AVANCE, 
            BP_PLAN_INDICATIVO_3.DESCRIPCION_INDICADOR
            FROM SOLICITUD TEMP_SOLICITUD 
            LEFT JOIN BP_PLAN_INDICATIVO 
              ON TEMP_SOLICITUD.COMPANIA = BP_PLAN_INDICATIVO.COMPANIA 
	            AND TEMP_SOLICITUD.ID_META_PRODUCTO = BP_PLAN_INDICATIVO.ID 
	            AND TEMP_SOLICITUD.VIGENCIA_PLAN_INDICATIVO = BP_PLAN_INDICATIVO.VIGENCIA_INICIAL 
            LEFT JOIN BP_PLAN_INDICATIVO BP_PLAN_INDICATIVO_1 
              ON TEMP_SOLICITUD.COMPANIA = BP_PLAN_INDICATIVO_1.COMPANIA
              AND TEMP_SOLICITUD.EJE = BP_PLAN_INDICATIVO_1.ID
              AND TEMP_SOLICITUD.VIGENCIA_PLAN_INDICATIVO = BP_PLAN_INDICATIVO_1.VIGENCIA_INICIAL 
            LEFT JOIN BP_PLAN_INDICATIVO  BP_PLAN_INDICATIVO_2 
            ON TEMP_SOLICITUD.COMPANIA = BP_PLAN_INDICATIVO_2.COMPANIA 
              AND TEMP_SOLICITUD.PROGRAMA = BP_PLAN_INDICATIVO_2.ID
              AND TEMP_SOLICITUD.VIGENCIA_PLAN_INDICATIVO = BP_PLAN_INDICATIVO_2.VIGENCIA_INICIAL
            LEFT JOIN BP_PLAN_INDICATIVO BP_PLAN_INDICATIVO_3 
              ON TEMP_SOLICITUD.METARESULTADO = BP_PLAN_INDICATIVO_3.ID 
              AND TEMP_SOLICITUD.COMPANIA = BP_PLAN_INDICATIVO_3.COMPANIA
              AND TEMP_SOLICITUD.VIGENCIA_PLAN_INDICATIVO = BP_PLAN_INDICATIVO_3.VIGENCIA_INICIAL
            LEFT JOIN BP_PLAN_INDICATIVO  BP_PLAN_INDICATIVO_4 
              ON TEMP_SOLICITUD.SECTOR = BP_PLAN_INDICATIVO_4.ID 
              AND TEMP_SOLICITUD.COMPANIA = BP_PLAN_INDICATIVO_4.COMPANIA
              AND TEMP_SOLICITUD.VIGENCIA_PLAN_INDICATIVO = BP_PLAN_INDICATIVO_4.VIGENCIA_INICIAL
            INNER JOIN PROYECTOS 
              ON TEMP_SOLICITUD.COMPANIA = PROYECTOS.COMPANIA
              AND TEMP_SOLICITUD.PROYECTO = PROYECTOS.CODIGO
            WHERE 2012>= TEMP_SOLICITUD.VIGENCIA_PLAN_INDICATIVO
            GROUP BY PROYECTOS.VALOREJECUTADO, 
            TEMP_SOLICITUD.COMPANIA, 
            TEMP_SOLICITUD.ID_META_PRODUCTO,
            BP_PLAN_INDICATIVO.DESCRIPCION, 
            TEMP_SOLICITUD.EJE,
            BP_PLAN_INDICATIVO_1.DESCRIPCION,
            TEMP_SOLICITUD.SECTOR,
            BP_PLAN_INDICATIVO_4.DESCRIPCION,
            TEMP_SOLICITUD.PROGRAMA,
            BP_PLAN_INDICATIVO_2.DESCRIPCION,
            BP_PLAN_INDICATIVO_3.DESCRIPCION, 
            TEMP_SOLICITUD.METARESULTADO, 
            BP_PLAN_INDICATIVO_1.VIGENCIA_INICIAL, 
            BP_PLAN_INDICATIVO.VIGENCIA_FINAL, 
            CASE WHEN NVL(PROYECTOS.VALOREJECUTADO, 0) = 0 THEN 0 ELSE
                      PROYECTOS.VALOREJECUTADO/PROYECTOS.VALORPROGRAMADO END,
            BP_PLAN_INDICATIVO_3.DESCRIPCION_INDICADOR)
SELECT BPNOVEDADPROYECTO.FECHA, 
 BPNOVEDADPROYECTO.OBJETO,
 SOLICITUD_DATOS.DESCRIPCION_EJE,
 SOLICITUD_DATOS.DESCRIPCION_SECTOR,
 SOLICITUD_DATOS.DESCRIPCION_PROGRAMA, 
 PROYECTOS.CODIGOBPIM, 
 PROYECTOS.NOMBREPROYECTO,
 SOLICITUD_DATOS.DESCRIPCION_METAPRODUCTO,
 BP_PLAN_INDICATIVO_METAS.CANTIDAD_PROGRAMADA, 
 BP_D_NOVEDADPROYECTO.CANTIDAD_PLAN,
 COMPONENTES.NOMBRECOMPONENTE, 
 BPNOVEDADPROYECTO.NECESIDADN, 
 BPNOVEDADPROYECTO.TIEMPOEJECUCION,
 TERCERO.NOMBRE NOMBRE_RESPONSABLE,
 PLAN_PRESUPUESTAL.CODIGO RUBROPRESUPUESTAL, 
 PLAN_PRESUPUESTAL.NOMBRE NOMBRE_RUBRO, 
 RESPONSABLE.CARGO CARGO_REPONSABLE,
 BP_D_NOVEDADPROYECTO.FUENTERECURSOS, 
 PCK_SYSMAN_UTL.FC_VALOR_LETRAS(BPNOVEDADPROYECTO.VALORTOTAL, BPNOVEDADPROYECTO.VALORTOTAL) VALORLETRAS,
 FUENTE_RECURSOS.NOMBRE NOMBRE_FUENTE,
 BP_PLAN_INDICATIVO_METAS.VIGENCIA_META, 
 BPNOVEDADPROYECTO.VALORTOTAL, 
 SOLICITUD_DATOS.META_RESULTADO
 FROM BPNOVEDADPROYECTO 
 LEFT JOIN BP_D_NOVEDADPROYECTO 
  ON BPNOVEDADPROYECTO.DEPENDENCIA = BP_D_NOVEDADPROYECTO.DEPENDENCIA
  AND BPNOVEDADPROYECTO.CODIGO = BP_D_NOVEDADPROYECTO.NOVEDAD
  AND BPNOVEDADPROYECTO.CLASET = BP_D_NOVEDADPROYECTO.CLASET 
  AND BPNOVEDADPROYECTO.TIPOT = BP_D_NOVEDADPROYECTO.TIPOT 
  AND BPNOVEDADPROYECTO.COMPANIA = BP_D_NOVEDADPROYECTO.COMPANIA 
 LEFT JOIN PROYECTOS
  ON BP_D_NOVEDADPROYECTO.COMPANIA = PROYECTOS.COMPANIA
  AND BP_D_NOVEDADPROYECTO.PROYECTO = PROYECTOS.CODIGO
 LEFT JOIN SOLICITUD_DATOS
  ON BP_D_NOVEDADPROYECTO.COMPANIA = SOLICITUD_DATOS.COMPANIA 
 LEFT JOIN COMPONENTES
  ON BP_D_NOVEDADPROYECTO.COMPANIA = COMPONENTES.COMPANIA 
  AND BP_D_NOVEDADPROYECTO.PROYECTO = COMPONENTES.CODIGOPROYECTO 
  AND BP_D_NOVEDADPROYECTO.COMPONENTE = COMPONENTES.CODIGO 
  AND BP_D_NOVEDADPROYECTO.TIPOCOMPONENTE = COMPONENTES.TIPOCOMPONENTE 
 LEFT JOIN DEPENDENCIA_RESPONSABLE 
  ON BPNOVEDADPROYECTO.COMPANIA = DEPENDENCIA_RESPONSABLE.COMPANIA 
  AND BPNOVEDADPROYECTO.DEPENDENCIA = DEPENDENCIA_RESPONSABLE.DEPENDENCIA
  AND BPNOVEDADPROYECTO.RESPONSABLE = DEPENDENCIA_RESPONSABLE.RESPONSABLE 
 LEFT JOIN RESPONSABLE 
  ON DEPENDENCIA_RESPONSABLE.COMPANIA = RESPONSABLE.COMPANIA 
  AND DEPENDENCIA_RESPONSABLE.RESPONSABLE = RESPONSABLE.CEDULA
 LEFT JOIN TERCERO  
  ON RESPONSABLE.COMPANIA = TERCERO.COMPANIA 
  AND RESPONSABLE.CEDULA = TERCERO.NIT 
  AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
 LEFT JOIN TEMP_SOLICITUD 
  ON BP_D_NOVEDADPROYECTO.COMPANIA = TEMP_SOLICITUD.COMPANIA 
 LEFT JOIN BP_PLAN_INDICATIVO_METAS 
  ON TEMP_SOLICITUD.COMPANIA = BP_PLAN_INDICATIVO_METAS.COMPANIA 
  AND TEMP_SOLICITUD.ID_META_PRODUCTO = BP_PLAN_INDICATIVO_METAS.ID_PLAN
 LEFT JOIN FUENTE_RECURSOS 
  ON BP_D_NOVEDADPROYECTO.COMPANIA = FUENTE_RECURSOS.COMPANIA 
  AND BP_D_NOVEDADPROYECTO.FUENTERECURSOS = FUENTE_RECURSOS.CODIGO 
 LEFT JOIN PLAN_PRESUPUESTAL 
  ON BP_D_NOVEDADPROYECTO.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA 
  AND BP_D_NOVEDADPROYECTO.RUBROPRESUPUESTAL = PLAN_PRESUPUESTAL.CODIGO
 WHERE BP_PLAN_INDICATIVO_METAS.VIGENCIA_META= s$vigencia$s            
 AND BP_D_NOVEDADPROYECTO.COMPANIA= s$compania$s
 AND BP_D_NOVEDADPROYECTO.TIPOT = 's$tipot$s'
 AND BP_D_NOVEDADPROYECTO.CLASET = 's$claset$s'
 AND BP_D_NOVEDADPROYECTO.NOVEDAD = s$novedad$s
 AND BP_D_NOVEDADPROYECTO.DEPENDENCIA = 's$dependencia$s'
