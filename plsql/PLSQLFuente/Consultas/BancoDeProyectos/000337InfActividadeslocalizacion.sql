SELECT DISTINCT TO_CHAR(PROYECTOS.FECHAREGISTRO, 'DD/MM/YYYY') FECHAREGISTRO,  
              PROYECTOS.NOMBREPROYECTO,  
              PROYECTOS.DEPENDENCIA,  
              PROYECTOLOCALIZACION.UBICACION,  
              PROYECTOS.CODIGOBPIM,  
              PROYECTOS.CANTIDAD,  
              DEPENDENCIA.NOMBRE NOMBREDEPENDENCIA,  
              PROYECTOS.OBJETO OBJ,  
              CASE WHEN 's$vigenciaInicial$s'='TODAS' THEN 's$vigenciaInicial$s' ELSE TO_CHAR(PROYECTOS.VIGENCIAINICIO) END VV,  
              PROYECTOS.VIGENCIAINICIO,  
              BARRIOS.NOMBRE BARRIOSNOMBRE,  
              PROYECTOS.VALOREJECUTADO VALORE,  
              CASE WHEN 's$barrioUbicacion$s'='X' THEN 's$barrioUbicacion$s' ELSE TO_CHAR(PROYECTOLOCALIZACION.BARRIO) END LOOC,  
              PROYECTOLOCALIZACION.BARRIO,  
              (PROYECTOS.VIGENCIAFIN - PROYECTOS.VIGENCIAINICIO) +1 PLAZO, 
              CASE PROYECTOS.ESTADOACTUAL WHEN 'E' THEN 'Ejecución' WHEN 'S' THEN 'Sin ejecutar' WHEN 'P' THEN 'Ejecutado parcialmente' WHEN  'T' THEN 'Ejecutado' ELSE 'Otro' END ESTADOACTUAL, 
              CASE WHEN 's$estadoActual$s'='X' THEN 's$estadoActual$s' ELSE PROYECTOS.ESTADOACTUAL END ESTADO 
FROM PROYECTOS LEFT JOIN PROYECTOLOCALIZACION  
  ON PROYECTOS.COMPANIA   = PROYECTOLOCALIZACION.COMPANIA  
 AND PROYECTOS.CODIGO     = PROYECTOLOCALIZACION.CODIGOPROYECTO  
LEFT JOIN BARRIOS ON  
        PROYECTOLOCALIZACION.BARRIO       = BARRIOS.CODIGO  
		AND PROYECTOLOCALIZACION.CIUDAD       = BARRIOS.CIUDAD  
		AND PROYECTOLOCALIZACION.DEPARTAMENTO = BARRIOS.DEPARTAMENTO  
		AND PROYECTOLOCALIZACION.PAIS         = BARRIOS.PAIS  
LEFT JOIN DEPENDENCIA ON  
        PROYECTOS.COMPANIA    = DEPENDENCIA.COMPANIA 
		AND PROYECTOS.DEPENDENCIA = DEPENDENCIA.CODIGO  
WHERE PROYECTOS.COMPANIA          = s$compania$s  
	AND PROYECTOS.CODIGO BETWEEN    's$proyectoInicial$s' AND 's$proyectoFin$s'      
	AND TO_CHAR(PROYECTOS.VIGENCIAINICIO)    = CASE WHEN 's$vigenciaInicial$s' = 'TODAS' THEN TO_CHAR(PROYECTOS.VIGENCIAINICIO) ELSE 's$vigenciaInicial$s' END 
	AND PROYECTOS.ESTADOACTUAL      = CASE WHEN 's$estadoActual$s' = 'X' THEN PROYECTOS.ESTADOACTUAL ELSE 's$estadoActual$s' END 
	AND PROYECTOLOCALIZACION.BARRIO = CASE WHEN 's$barrioUbicacion$s' = 'X' THEN PROYECTOLOCALIZACION.BARRIO ELSE 's$barrioUbicacion$s' END 
	AND PROYECTOS.DEPENDENCIA	= CASE WHEN 's$dependencia$s'	= 'X' THEN PROYECTOS.DEPENDENCIA ELSE 's$dependencia$s' END 
  AND PROYECTOS.ENVIADONACION     	= CASE WHEN 's$enviado$s' ='1' THEN -1 ELSE PROYECTOS.ENVIADONACION END 
  AND PROYECTOS.ENVIADODPTO       	= CASE WHEN 's$enviado$s' ='2' THEN -1 ELSE PROYECTOS.ENVIADODPTO END 
  AND PROYECTOS.ENVIADOOTRAENTIDAD	= CASE WHEN 's$enviado$s' ='3' THEN -1 ELSE PROYECTOS.ENVIADOOTRAENTIDAD END
