SELECT BPTIPOSNOVEDADTECNICA.CODIGO, 
    BPTIPOSNOVEDADTECNICA.NOMBRE, 
    BPTIPOSNOVEDADTECNICA.DESCRIPCION,
    (CASE WHEN BPTIPOSNOVEDADTECNICA.TIPO = 'EST' THEN 'Estudio' ELSE 
    (CASE WHEN BPTIPOSNOVEDADTECNICA.TIPO = 'LIC' THEN 'Licencia' ELSE 
    (CASE WHEN BPTIPOSNOVEDADTECNICA.TIPO = 'DIS' THEN 'Diseño' ELSE
    (CASE WHEN BPTIPOSNOVEDADTECNICA.TIPO = 'OTR' THEN 'Otros' ELSE
    (CASE WHEN BPTIPOSNOVEDADTECNICA.TIPO = 'NOA' THEN 'No Aplica'  
            END)
          END)
        END)
      END)
    END) TIPO
FROM BPTIPOSNOVEDADTECNICA 
WHERE BPTIPOSNOVEDADTECNICA.COMPANIA =  s$compania$s
ORDER BY BPTIPOSNOVEDADTECNICA.TIPO, 
    BPTIPOSNOVEDADTECNICA.CODIGO
