SELECT DISTINCT 
    PROYECTOS.COMPANIA, 
    PROGRAMACION.CODIGOPROYECTO, 
    PROGRAMACION.VIGENCIA  VIGENCIA1, 
    PROGRAMACION.VIGENCIA, 
    PROGRAMACION.TIPOESTADO, 
    PROGRAMACION.REPROGRAMADO, 
    SUM(PROGRAMACION.VALORTOTAL)  VT, 
    PROYECTOS.VIGENCIAINICIO, 
    PROYECTOS.VIGENCIAFIN
FROM PROGRAMACION 
  INNER JOIN PROYECTOS ON PROGRAMACION.COMPANIA = PROYECTOS.COMPANIA 
    AND PROGRAMACION.CODIGOPROYECTO = PROYECTOS.CODIGO 
WHERE PROYECTOS.COMPANIA          = $P{PR_COMPANIA}
  AND PROGRAMACION.CODIGOPROYECTO = $P{PR_CODIGOPROYECTO}
GROUP BY 
    PROYECTOS.COMPANIA, 
    PROGRAMACION.VIGENCIA, 
    PROGRAMACION.TIPOESTADO, 
    PROGRAMACION.REPROGRAMADO, 
    PROGRAMACION.CODIGOPROYECTO, 
    PROYECTOS.VIGENCIAINICIO, 
    PROYECTOS.VIGENCIAFIN, 
    PROGRAMACION.VIGENCIA, 
    PROGRAMACION.CODIGOPROYECTO 
ORDER BY 
    PROGRAMACION.VIGENCIA
