SELECT 
    BP_PLAN_INDICATIVO.ID, 
    BP_PLAN_INDICATIVO.DESCRIPCION, 
    BP_PLAN_INDICATIVO.VIGENCIA_INICIAL, 
    BP_PLAN_INDICATIVO.VIGENCIA_FINAL, 
    BP_PLAN_INDICATIVO.PONDERACION, 
    CASE BP_PLAN_INDICATIVO.TIPO_META_PLAN WHEN '001' THEN 'Meta de resultado' WHEN '002' THEN 'Meta de producto' END AS TIPO_META_PLAN, 
    BP_PLAN_INDICATIVO.UNIDAD_MEDIDA, 
    BP_PLAN_INDICATIVO.DESCRIPCION_INDICADOR 
FROM 
    BP_PLAN_INDICATIVO 
WHERE 
    BP_PLAN_INDICATIVO.COMPANIA =  s$compania$s
  
        AND 
    BP_PLAN_INDICATIVO.VIGENCIA_INICIAL = s$vigenciaGubernamental$s
ORDER BY 
    BP_PLAN_INDICATIVO.ID
