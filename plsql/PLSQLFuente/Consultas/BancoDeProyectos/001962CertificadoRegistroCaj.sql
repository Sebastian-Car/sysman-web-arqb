SELECT  DISTINCT
       PROYECTOS.COMPANIA,
       PROYECTOS.CODIGO CD,
       PROYECTOS.NUMERO_REGISTRO CODREGISTRO,
       CASE
         WHEN PROYECTOS.TIPOREGISTRO IN ('REG')
         THEN 'X'
         ELSE ' '
       END REG,
       CASE
         WHEN PROYECTOS.TIPOREGISTRO IN ('ACT')
         THEN 'X'
         ELSE ' '
       END ACT,
       PROYECTOS.NOMBREPROYECTO PROYECTO,
       PROYECTOS.CODIGOBPIM CODIGO,
       PROYECTOS.VIGENCIAFIN ANO,
       TO_CHAR(PROYECTOS.FECHAREGISTRO,'DD/MM/YYYY') FECCERTIFICADO,
       PROYECTOS.VALORTOTAL VALOR,
       PCK_SYSMAN_UTL.FC_VALOR_LETRAS(PROYECTOS.VALORTOTAL) VALORLETRA,
       'Dada en Cajicá a los '
       || EXTRACT(DAY FROM PROYECTOS.FECHAREGISTRO)
       || ' días del mes de '
       || PCK_SYSMAN_UTL.FC_NOMBRE_MES(EXTRACT(MONTH FROM PROYECTOS.FECHAREGISTRO))
       || ' del año '
       || EXTRACT(YEAR FROM PROYECTOS.FECHAREGISTRO) FECHADADA,
       USUARIO.NOMBRE1||' '||USUARIO.NOMBRE2||' '||USUARIO.APELLIDO1||' '||USUARIO.APELLIDO2 CREADOR
  FROM PROYECTOS
 INNER JOIN BP_PROYECTO_PLAN_INDICATIVO
    ON PROYECTOS.COMPANIA = BP_PROYECTO_PLAN_INDICATIVO.COMPANIA
   AND PROYECTOS.CODIGO   = BP_PROYECTO_PLAN_INDICATIVO.PROYECTO
 INNER JOIN BP_PLAN_INDICATIVO_METAS
    ON BP_PROYECTO_PLAN_INDICATIVO.COMPANIA        = BP_PLAN_INDICATIVO_METAS.COMPANIA
   AND BP_PROYECTO_PLAN_INDICATIVO.ID_PLAN_P       = BP_PLAN_INDICATIVO_METAS.ID_PLAN
   AND BP_PROYECTO_PLAN_INDICATIVO.VIGENCIA_PLAN_P = BP_PLAN_INDICATIVO_METAS.VIGENCIA_PLAN
   AND BP_PROYECTO_PLAN_INDICATIVO.VIGENCIA_META_P = BP_PLAN_INDICATIVO_METAS.VIGENCIA_META
 INNER JOIN BP_PLAN_INDICATIVO
    ON BP_PLAN_INDICATIVO_METAS.COMPANIA      = BP_PLAN_INDICATIVO.COMPANIA
   AND BP_PLAN_INDICATIVO_METAS.ID_PLAN       = BP_PLAN_INDICATIVO.ID
   AND BP_PLAN_INDICATIVO_METAS.VIGENCIA_PLAN = BP_PLAN_INDICATIVO.VIGENCIA_INICIAL
  LEFT JOIN USUARIO
    ON PROYECTOS.CREATED_BY = USUARIO.CODIGO
 WHERE PROYECTOS.COMPANIA  =  s$compania$s
   AND PROYECTOS.CODIGO    = s$codigo$s
