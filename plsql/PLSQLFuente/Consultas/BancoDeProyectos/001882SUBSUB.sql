SELECT DISTINCT 
      BP_PLAN_INDICATIVO.DESCRIPCION  SUB
FROM PROYECTOS
INNER JOIN BP_PROYECTO_PLAN_INDICATIVO
		ON PROYECTOS.COMPANIA = BP_PROYECTO_PLAN_INDICATIVO.COMPANIA
		AND PROYECTOS.CODIGO  = BP_PROYECTO_PLAN_INDICATIVO.PROYECTO
INNER JOIN BP_PLAN_INDICATIVO
		ON BP_PROYECTO_PLAN_INDICATIVO.COMPANIA         = BP_PLAN_INDICATIVO.COMPANIA
		AND SUBSTR(BP_PROYECTO_PLAN_INDICATIVO.ID_PLAN_P,1,s$digitosubprog$s) = BP_PLAN_INDICATIVO.ID
		AND BP_PROYECTO_PLAN_INDICATIVO.VIGENCIA_PLAN_P = BP_PLAN_INDICATIVO.VIGENCIA_INICIAL
WHERE  PROYECTOS.COMPANIA  =  s$compania$s
	AND PROYECTOS.CODIGO    = s$codigo$s
