SELECT  FUENTESFINANCIACION.CODIGOFUENTE AS FCODIGOFUENTE, 
        FUENTESFINANCIACION.NOMBRE AS FNOMBRE, 
        FUENTESFINANCIACION.DESCRIPCION AS FDESCRIPCION, 
        FUENTESFINANCIACION.SIGLA AS FSIGLA, 
        FUENTESFINANCIACION.CODIGODNP AS FCODIGODNP, 
       (  CASE
    WHEN FUENTESFINANCIACION.ORDEN = 'M'
    THEN 'MUNICIPAL'
    WHEN FUENTESFINANCIACION.ORDEN = 'N'
    THEN 'NACIONAL'
    WHEN FUENTESFINANCIACION.ORDEN = 'D'
    THEN 'DEPARTAMENTAL' END)AS FORDEN,
        SUBFUENTESFINANCIACION.CODIGOSUBFUENTE, 
        SUBFUENTESFINANCIACION.NOMBRE, 
        SUBFUENTESFINANCIACION.DESCRIPCION, 
        SUBFUENTESFINANCIACION.CODIGODNP
FROM FUENTESFINANCIACION 
  LEFT JOIN SUBFUENTESFINANCIACION ON FUENTESFINANCIACION.CODIGOFUENTE = SUBFUENTESFINANCIACION.CODIGOFUENTE 
    AND FUENTESFINANCIACION.COMPANIA = SUBFUENTESFINANCIACION.COMPANIA
WHERE FUENTESFINANCIACION.COMPANIA = s$compania$s 
  AND FUENTESFINANCIACION.CODIGOFUENTE BETWEEN 's$fuenteInicial$s' AND 's$fuenteFinal$s'
