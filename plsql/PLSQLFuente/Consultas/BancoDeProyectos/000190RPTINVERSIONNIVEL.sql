WITH RESUMEN AS ( 
SELECT  
    R.NIVEL,  
    R.DESCRIPCION,  
    S.COMPANIA, 
    S.ANO, 
    S.ID, 
    R.AUXILIAR, 
    (SUM(S.REG_CONTRACT) + SUM(S.MODIF_REG_CONT)) + (SUM(S.REG_NO_CONTRACT) + SUM(S.MODIF_REG_NOCONT)) AS TOTALCOMPROMETIDO,  
    SUM(CASE WHEN P.NATURALEZA='D' THEN APROPIACION_DEBITO-APROPIACION_CREDITO ELSE APROPIACION_CREDITO-APROPIACION_DEBITO END) APROPIADOINICIAL, 
     
    (SUM(CASE WHEN P.NATURALEZA='D' THEN APROPIACION_DEBITO-APROPIACION_CREDITO ELSE APROPIACION_CREDITO-APROPIACION_DEBITO END)) + SUM(S.ADICION)  
      + SUM(S.REDUCCION) + SUM(CASE WHEN P.NATURALEZA='D' THEN APLAZAM_DEBITO-APLAZAM_CREDITO ELSE APLAZAM_CREDITO-APLAZAM_DEBITO END )  
      + SUM(CASE WHEN P.NATURALEZA='D' THEN TRASLADO_DEBITO-TRASLADO_CREDITO ELSE TRASLADO_CREDITO-TRASLADO_DEBITO END) APRDEFINITIVA,  
       
    SUM(CASE WHEN P.NATURALEZA='D' THEN APROPIACION_DEBITO-APROPIACION_CREDITO ELSE APROPIACION_CREDITO-APROPIACION_DEBITO END)  
    + SUM(S.ADICION) + SUM(S.REDUCCION) + SUM(CASE WHEN P.NATURALEZA='D' THEN APLAZAM_DEBITO-APLAZAM_CREDITO ELSE APLAZAM_CREDITO-APLAZAM_DEBITO END )  
    + SUM(CASE WHEN P.NATURALEZA='D' THEN TRASLADO_DEBITO-TRASLADO_CREDITO ELSE TRASLADO_CREDITO-TRASLADO_DEBITO END)  
    + SUM(CASE WHEN P.NATURALEZA='D' THEN APLAZAM_DEBITO-APLAZAM_CREDITO ELSE APLAZAM_CREDITO-APLAZAM_DEBITO END ) - SUM(S.DISPONIBILIDAD) + SUM(S.REINTEGRO) SALDODISPONIBLE 
FROM BP_RUBRO_INVERSION_DET R  
  INNER JOIN (V_PLAN_PRESUPUESTAL P LEFT JOIN V_SALDO_PLAN_PPTAL S ON P.COMPANIA = S.COMPANIA 
                                                                  AND P.ID = S.ID  
                                                                  AND P.ANO = S.ANO)  
      ON R.COMPANIA = P.COMPANIA 
      AND R.RUBRO = P.ID 
      AND R.VIGENCIA = P.ANO 
WHERE R.COMPANIA=s$compania$s   
    AND R.VIGENCIA BETWEEN s$anioIncial$s AND s$anioFinal$s  
    AND R.NIVEL='s$nivel$s'  
    AND DESCRIPCION = 's$descripcion$s' 
GROUP BY R.NIVEL, R.DESCRIPCION, S.COMPANIA, S.ANO, S.ID, P.NATURALEZA, R.AUXILIAR 
) 
SELECT 
  RESUMEN.COMPANIA,  
  RESUMEN.ANO, 
  BP_RUBRO_INVERSION_DET.NIVEL,  
  BP_RUBRO_INVERSION_DET.DESCRIPCION, 
  SUM(RESUMEN.APROPIADOINICIAL) AS APROPIADOINICIAL, 
  SUM(RESUMEN.APRDEFINITIVA) AS APRDEFINITIVA,  
  SUM(RESUMEN.TOTALCOMPROMETIDO) AS TOTALCOMPROMETIDO, 
  SUM(RESUMEN.SALDODISPONIBLE) AS SALDODISPONIBLE 
FROM RESUMEN 
  INNER JOIN BP_RUBRO_INVERSION_DET ON RESUMEN.COMPANIA = BP_RUBRO_INVERSION_DET.COMPANIA 
  AND RESUMEN.ID = BP_RUBRO_INVERSION_DET.RUBRO 
  AND RESUMEN.AUXILIAR = BP_RUBRO_INVERSION_DET.AUXILIAR 
    AND RESUMEN.ANO = BP_RUBRO_INVERSION_DET.VIGENCIA 
GROUP BY RESUMEN.COMPANIA, RESUMEN.ANO, BP_RUBRO_INVERSION_DET.NIVEL, BP_RUBRO_INVERSION_DET.DESCRIPCION ORDER BY RESUMEN.ANO
