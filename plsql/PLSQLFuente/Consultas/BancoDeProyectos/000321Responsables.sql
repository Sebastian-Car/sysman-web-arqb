SELECT 
    RESPONSABLE.CEDULA, 
    TERCERO.NOMBRE, 
    RESPONSABLE.CARGO                
FROM 
    RESPONSABLE       
        INNER JOIN TERCERO 
          ON TERCERO.COMPANIA = RESPONSABLE.COMPANIA  
         AND TERCERO.NIT = RESPONSABLE.CEDULA  
         AND TERCERO.SUCURSAL = RESPONSABLE.SUCURSAL   
WHERE 
    RESPONSABLE.COMPANIA = s$compania$s
