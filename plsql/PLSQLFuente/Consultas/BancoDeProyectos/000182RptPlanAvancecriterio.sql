WITH AVANCE_CUMPLIMIENTO AS (s$query$s) 
    SELECT BP_PLAN_INDICATIVO.COMPANIA,
    BP_PLAN_INDICATIVO.ID,
    BP_PLAN_INDICATIVO.VIGENCIA_INICIAL,
    BP_PLAN_INDICATIVO.VIGENCIA_FINAL,
    AVANCE_CUMPLIMIENTO.CUMPLIMIENTO,
    BP_PLAN_INDICATIVO.DESCRIPCION,
    SUM(AVANCE_CUMPLIMIENTO.PONDERACION) AS PONDERACION,
    SUM(AVANCE_CUMPLIMIENTO.AVANCE) AS AVANCE,
    SUM(AVANCE_CUMPLIMIENTO.CUENTO) AS TOTAL
    FROM BP_PLAN_INDICATIVO 
        INNER JOIN AVANCE_CUMPLIMIENTO 
            ON BP_PLAN_INDICATIVO.COMPANIA = AVANCE_CUMPLIMIENTO.COMPANIA
            AND BP_PLAN_INDICATIVO.VIGENCIA_INICIAL = AVANCE_CUMPLIMIENTO.VIGENCIA_INICIAL
    WHERE BP_PLAN_INDICATIVO.COMPANIA =s$compania$s
    AND LENGTH(BP_PLAN_INDICATIVO.ID) = s$nivel$s
    AND BP_PLAN_INDICATIVO.ID = SUBSTR(AVANCE_CUMPLIMIENTO.ID, 1, s$nivel$s) 
    AND BP_PLAN_INDICATIVO.VIGENCIA_INICIAL=  s$anio$s   
    GROUP BY BP_PLAN_INDICATIVO.COMPANIA, BP_PLAN_INDICATIVO.ID,
    BP_PLAN_INDICATIVO.VIGENCIA_INICIAL, BP_PLAN_INDICATIVO.VIGENCIA_FINAL,
    AVANCE_CUMPLIMIENTO.CUMPLIMIENTO, BP_PLAN_INDICATIVO.DESCRIPCION
