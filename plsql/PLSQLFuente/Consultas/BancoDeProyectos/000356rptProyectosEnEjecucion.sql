SELECT P.COMPANIA,
      P.VIGENCIAINICIO,
      P.VIGENCIAFIN,
      P.CODIGO, 
      P.TIPOPROYECTO, 
      P.CODIGOBPIM, 
      P.NOMBREPROYECTO,
      P.DEPENDENCIA, 
      P.RESPONSABLE, 
      P.CARGORESPONSABLE, 
      P.VIGENCIAINICIO, 
      P.VIGENCIAFIN,
      D.NOMBRE NOM_DEPENDENCIA,
      TERCERO.NOMBRE NOM_RESPONSABLE
FROM PROYECTOS P 
	LEFT JOIN DEPENDENCIA D ON  
			P.COMPANIA        = D.COMPANIA 
		AND P.DEPENDENCIA   = D.CODIGO 
	LEFT JOIN RESPONSABLE R ON  
			P.COMPANIA        = R.COMPANIA
		AND P.RESPONSABLE   = R.CEDULA
		AND P.SUCURSAL		= R.SUCURSAL
  INNER JOIN TERCERO ON
      R.COMPANIA        = TERCERO.COMPANIA
    AND R.CEDULA        = TERCERO.NIT
    AND R.SUCURSAL      = TERCERO.SUCURSAL
WHERE P.COMPANIA        = s$compania$s  
	AND P.CODIGO BETWEEN 's$proyectoInicial$s' AND 's$proyectoFin$s'
	AND P.DEPENDENCIA     = CASE WHEN 's$dependencia$s' = 'X' THEN P.DEPENDENCIA ELSE 's$dependencia$s' END
	AND P.VIGENCIAINICIO  <= CASE WHEN 's$vigenciaFin$s' = 'TODAS' THEN TO_CHAR(P.VIGENCIAINICIO) ELSE 's$vigenciaFin$s' END 
	AND P.VIGENCIAFIN     >= CASE WHEN 's$vigenciaInicio$s' = 'TODAS' THEN TO_CHAR(P.VIGENCIAFIN) ELSE 's$vigenciaInicio$s' END
	AND P.ESTADOACTUAL = 'E'
