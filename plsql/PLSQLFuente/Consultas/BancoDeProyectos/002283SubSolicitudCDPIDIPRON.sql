    ON V_SOLICITUD_CDP.COMPANIA         = FUENTE_RECURSOS.COMPANIA 
    AND V_SOLICITUD_CDP.VIGENCIA         = FUENTE_RECURSOS.ANO
   AND V_SOLICITUD_CDP.FUENTERECURSOS        = FUENTE_RECURSOS.CODIGO
