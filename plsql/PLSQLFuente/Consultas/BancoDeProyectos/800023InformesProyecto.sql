WITH AUX AS (
        SELECT VALORPROGRAMADO,
        PROYECTOS.CODIGOBPIM, 
        NVL(PROYECTOS.NOMBREPROYECTO,' ') NOMBREPROYECTO, 
        PROYECTOS.DEPENDENCIA, 
        PROYECTOS.VALORTOTAL, 
        DEPENDENCIA.NOMBRE NOMBRESECRETARIA, 
        TERCERO.NOMBRE NOMBRERESPONSABLE, 
        NVL(VIGENCIAINICIO,EXTRACT (YEAR FROM SYSDATE)) ANNO1, 
        BP_PROYECTOSRUBROS.PROYECTO, 
        V_PLAN_PRESUPUESTAL.ANO DIM, 
        V_PLAN_PRESUPUESTAL_1.ANO PROG, 
        V_PLAN_PRESUPUESTAL_2.ANO SUBPRO, 
        V_PLAN_PRESUPUESTAL_3.ANO SEC, 
        NVL(V_PLAN_PRESUPUESTAL.NOMBRE, ' ') NOMBREDIMENSION, 
        NVL(V_PLAN_PRESUPUESTAL_1.NOMBRE,' ') NOMBREPROGRAMA, 
        NVL(V_PLAN_PRESUPUESTAL_2.NOMBRE,' ') NOMBRESUBPROGRAMA, 
        NVL(V_PLAN_PRESUPUESTAL_3.NOMBRE, ' ') NOMBRESECTOR, 
        PROYECTOS.VIGENCIAINICIO, 
        PROYECTOS.COMPANIA, 
        PROYECTOS.CODIGO  
        FROM PROYECTOS 
          LEFT JOIN BP_PROYECTOSRUBROS ON 
              PROYECTOS.COMPANIA = BP_PROYECTOSRUBROS.COMPANIA
            AND PROYECTOS.CODIGO = BP_PROYECTOSRUBROS.PROYECTO 
          LEFT JOIN V_PLAN_PRESUPUESTAL ON 
              BP_PROYECTOSRUBROS.COMPANIA = V_PLAN_PRESUPUESTAL.COMPANIA
            AND BP_PROYECTOSRUBROS.DIMENSION = V_PLAN_PRESUPUESTAL.ID
          LEFT JOIN V_PLAN_PRESUPUESTAL V_PLAN_PRESUPUESTAL_1 ON 
              BP_PROYECTOSRUBROS.COMPANIA = V_PLAN_PRESUPUESTAL_1.COMPANIA
            AND BP_PROYECTOSRUBROS.PROGRAMA = V_PLAN_PRESUPUESTAL_1.ID
          LEFT JOIN V_PLAN_PRESUPUESTAL V_PLAN_PRESUPUESTAL_2 ON 
              BP_PROYECTOSRUBROS.COMPANIA = V_PLAN_PRESUPUESTAL_2.COMPANIA
            AND BP_PROYECTOSRUBROS.SUBPROGRAMA = V_PLAN_PRESUPUESTAL_2.ID 
          LEFT JOIN V_PLAN_PRESUPUESTAL V_PLAN_PRESUPUESTAL_3 ON 
              BP_PROYECTOSRUBROS.COMPANIA = V_PLAN_PRESUPUESTAL_3.COMPANIA
            AND BP_PROYECTOSRUBROS.SECTOR = V_PLAN_PRESUPUESTAL_3.ID
          LEFT JOIN DEPENDENCIA ON 
              PROYECTOS.COMPANIA = DEPENDENCIA.COMPANIA
            AND PROYECTOS.DEPENDENCIA = DEPENDENCIA.CODIGO
          LEFT JOIN RESPONSABLE ON 
              PROYECTOS.COMPANIA = RESPONSABLE.COMPANIA
            AND PROYECTOS.RESPONSABLE = RESPONSABLE.CEDULA
          LEFT JOIN TERCERO ON
              RESPONSABLE.COMPANIA = TERCERO.COMPANIA
            AND RESPONSABLE.CEDULA = TERCERO.NIT
            AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
        WHERE PROYECTOS.COMPANIA= s$compania$s
        AND PROYECTOS.CODIGO='s$proyecto$s'
)
SELECT * FROM AUX
WHERE ROWNUM =1
