SELECT BPDNOVEDADPROYECTO.COMPANIA,
  BPDNOVEDADPROYECTO.TIPOT,
  BPDNOVEDADPROYECTO.CLASET,
  BPDNOVEDADPROYECTO.NOVEDAD,
  BPDNOVEDADPROYECTO.PROYECTO,
  BPDNOVEDADPROYECTO.COMPONENTE,
  BPDNOVEDADPROYECTO.ACTIVIDAD,
  BPDNOVEDADPROYECTO.ID_META_PRODUCTO,
  BPDNOVEDADPROYECTO.CANTIDAD_PLAN,
  SUBSTR(ID_META_PRODUCTO,1,s$eje$s) AS EJE,
  SUBSTR(ID_META_PRODUCTO,1,s$sector$s) AS SECTOR,
  SUBSTR(ID_META_PRODUCTO,1,s$programa$s) AS PROGRAMA,
  SUBSTR(ID_META_PRODUCTO,1,s$metaResul$s) AS METARESULTADO,
  BPDNOVEDADPROYECTO.VIGENCIA_PLAN_INDICATIVO
FROM BP_D_NOVEDADPROYECTO BPDNOVEDADPROYECTO
WHERE BPDNOVEDADPROYECTO.COMPANIA = s$compania$s
  AND BPDNOVEDADPROYECTO.TIPOT      ='s$tipot$s'
  AND BPDNOVEDADPROYECTO.CLASET     = 's$claset$s'
  AND BPDNOVEDADPROYECTO.NOVEDAD    = s$novedad$s
  AND BPDNOVEDADPROYECTO.DEPENDENCIA= 's$dependencia$s'
GROUP BY BPDNOVEDADPROYECTO.COMPANIA,
  BPDNOVEDADPROYECTO.TIPOT,
  BPDNOVEDADPROYECTO.CLASET,
  BPDNOVEDADPROYECTO.NOVEDAD,
  BPDNOVEDADPROYECTO.PROYECTO,
  BPDNOVEDADPROYECTO.COMPONENTE,
  BPDNOVEDADPROYECTO.ACTIVIDAD,
  BPDNOVEDADPROYECTO.ID_META_PRODUCTO,
  BPDNOVEDADPROYECTO.CANTIDAD_PLAN,
  SUBSTR(ID_META_PRODUCTO,1, s$eje$s ),
  SUBSTR(ID_META_PRODUCTO,1, s$sector$s),
  SUBSTR(ID_META_PRODUCTO,1, s$programa$s ),
  SUBSTR(ID_META_PRODUCTO,1, s$metaResul$s ),
  BPDNOVEDADPROYECTO.VIGENCIA_PLAN_INDICATIVO 
