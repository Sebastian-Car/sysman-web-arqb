WITH INVERSION AS ( 
      SELECT  
        S.COMPANIA,  
        S.ANO,  
        APROPIADO,  
        APROPIADO+ADICION+REDUCCION+APLAZAMIENTO+TRASLADO APRDEFINITIVA, 
        SUM(REG_CONTRACT+MODIF_REG_CONT+REG_NO_CONTRACT+MODIF_REG_NOCONT) TOTALCOMPROMETIDO, 
        APROPIADO+ADICION+REDUCCION+APLAZAMIENTO+TRASLADO+REINTEGRO-DISPONIBILIDAD SALDODISPONIBLE 
      FROM BP_RUBRO_INVERSION R  
        INNER JOIN V_RESUMENPPTO_BASE S 
            ON R.COMPANIA = S.COMPANIA 
          AND R.RUBRO = S.ID 
          AND R.VIGENCIA = S.ANO 
      WHERE S.COMPANIA = s$compania$s  
        AND  S.VIGENCIA BETWEEN s$anioInicial$s AND s$anioFinal$s  
      GROUP BY S.COMPANIA, S.ANO, ID, MES, ADICION,  
        REDUCCION, DISPONIBILIDAD, REINTEGRO, NATURALEZA, 
        TRASLADO, APROPIADO, APLAZAMIENTO, REG_CONTRACT, 
        MODIF_REG_CONT, MODIF_REG_NOCONT,  
        APROPIADO+ADICION+REDUCCION+APLAZAMIENTO+TRASLADO+REINTEGRO-DISPONIBILIDAD 
		) 
   SELECT COMPANIA, 
       ANO, 
       SUM (TOTALCOMPROMETIDO)TOTALCOMPROMETIDO, 
       SUM (APROPIADO)APROPIADOINICIAL, 
       SUM (APRDEFINITIVA) APRDEFINITIVA, 
       SUM (SALDODISPONIBLE) SALDODISPONIBLE 
   FROM INVERSION 
   GROUP BY COMPANIA, ANO 
   ORDER BY ANO ASC
