SELECT BP_PLAN_INDICATIVO.COMPANIA, 
BP_PLAN_INDICATIVO.ID, 
BP_PLAN_INDICATIVO.VIGENCIA_INICIAL, 
BP_PLAN_INDICATIVO.VIGENCIA_FINAL, 
BP_PLAN_INDICATIVO.DESCRIPCION, 
BP_PLAN_INDICATIVO.PONDERACION ACTUAL, 
TEMP_PLAN_INDICATIVO.PONDERACION FINAL, 
BP_PLAN_INDICATIVO.PONDERACION
FROM TEMP_PLAN_INDICATIVO 
    INNER JOIN BP_PLAN_INDICATIVO ON 
            TEMP_PLAN_INDICATIVO.COMPANIA = BP_PLAN_INDICATIVO.COMPANIA
        AND TEMP_PLAN_INDICATIVO.ID = BP_PLAN_INDICATIVO.ID
        AND TEMP_PLAN_INDICATIVO.VIGENCIA_INICIAL = BP_PLAN_INDICATIVO.VIGENCIA_INICIAL
WHERE TEMP_PLAN_INDICATIVO.PONDERACION <> BP_PLAN_INDICATIVO.PONDERACION
