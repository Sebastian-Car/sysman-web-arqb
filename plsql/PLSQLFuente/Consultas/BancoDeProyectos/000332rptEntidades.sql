SELECT  BP_ENTIDADES.CODIGO,
        BP_ENTIDADES.NOMBRE,
        BP_ENTIDADES.SIGLADNP,
        CASE BP_ENTIDADES.ORDEN WHEN 'N' THEN 'Nacional' WHEN 'D' THEN 'Departamental' WHEN 'M' THEN 'Municipal' END AS ORDEN,
        SECTORDNP.NOMBRE AS SECTORDNP,
        CASE BP_ENTIDADES.TIPOENTIDAD WHEN 'EC' THEN 'Entidad Centralizada' WHEN 'EP' THEN 'Establecimiento Público' WHEN 'ST' THEN 'Sin Tipo' END AS TIPOENTIDAD
FROM    BP_ENTIDADES
  LEFT JOIN SECTORDNP ON
    BP_ENTIDADES.COMPANIA = SECTORDNP.COMPANIA 
    AND BP_ENTIDADES.SECTORDNP = CODIGODNP
WHERE   BP_ENTIDADES.COMPANIA =   s$compania$s
