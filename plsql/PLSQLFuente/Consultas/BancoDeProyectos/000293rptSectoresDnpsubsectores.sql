SELECT SECTORDNP.NOMBRE SNOMBRE,
  SECTORDNP.SIGLA SSIGLA,
  SECTORDNP.OBJETIVOS SOBJETIVOS,
  SUBSECTORDNP.CODIGODNP,
  SUBSECTORDNP.NOMBRE,
  SUBSECTORDNP.SIGLA,
  SUBSECTORDNP.OBJETIVOS,
  SUBSECTORDNP.META
FROM SECTORDNP
LEFT JOIN SUBSECTORDNP
ON SECTORDNP.CODIGODNP   = SUBSECTORDNP.CODIGOSECTORDNP
AND SECTORDNP.COMPANIA   = SUBSECTORDNP.COMPANIA
WHERE SECTORDNP.COMPANIA = 's$compania$s' AND SECTORDNP.CODIGODNP BETWEEN 's$sectorDnp$s' AND 's$sectorDnpf$s' ORDER BY SECTORDNP.NOMBRE
