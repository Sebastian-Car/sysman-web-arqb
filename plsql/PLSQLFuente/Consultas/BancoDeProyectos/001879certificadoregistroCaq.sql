WITH REGISTROFECHA AS
  (SELECT COMPANIA, CODIGO,
    CASE
      WHEN TIPOREGISTRO IN ('REG')
      THEN FECHAREGISTRO
      ELSE FECHA_ACTUALIZACION
    END FECHAREG
  FROM PROYECTOS
  )
SELECT 
    PROYECTOS.CREATED_BY CREADOR,
    CASE WHEN USUARIO.NOMBRE1 IN ('') THEN '' ELSE USUARIO.NOMBRE1 ||' ' END||
    CASE WHEN USUARIO.NOMBRE2 IN ('') THEN '' ELSE USUARIO.NOMBRE2 ||' ' END ||
    CASE WHEN USUARIO.APELLIDO1 IN ('') THEN '' ELSE USUARIO.APELLIDO1 ||' 'END ||
    CASE WHEN USUARIO.APELLIDO2 IN ('') THEN '' ELSE USUARIO.APELLIDO2  ||' ' END NOMBRELARGO,
PROYECTOS.CODIGO,
			  PROYECTOS.CODIGOBPIM,
			  PROYECTOS.NOMBREPROYECTO,
			  PROYECTOS.DEPENDENCIA,
			  PROYECTOS.RESPONSABLE,
			  PROYECTOS.CARGORESPONSABLE,
			  PROYECTOS.OBJETO           OBJETO_PROYECTO,
			  PROYECTOS.NUMERO_REGISTRO,
			  PROYECTOS.TIPOREGISTRO,
			  DEPENDENCIA.NOMBRE      NOMBRE_DEPENDENCIA,
	      TERCERO.NOMBRE     NOMBRE_RESPONSABLE,
			  PROYECTOS.VALORTOTAL,
       'Florencia, ' || EXTRACT(DAY FROM REGISTROFECHA.FECHAREG) || ' De ' ||  
          PCK_SYSMAN_UTL.FC_NOMBRE_MES(EXTRACT(MONTH FROM REGISTROFECHA.FECHAREG)) || ' De ' || 
          EXTRACT(YEAR FROM REGISTROFECHA.FECHAREG) FECHAF,
          
        CASE WHEN TIPOREGISTRO IN ('REG') THEN 'Radico' ELSE 'Actualizo' END  DESCRIPCION
,EXTRACT(DAY FROM REGISTROFECHA.FECHAREG)
  || ' dias del mes de '
  || PCK_SYSMAN_UTL.FC_NOMBRE_MES(EXTRACT(MONTH FROM REGISTROFECHA.FECHAREG))
  || ' De '
  || EXTRACT(YEAR FROM REGISTROFECHA.FECHAREG) FECHAH
FROM PROYECTOS
			LEFT JOIN DEPENDENCIA
			ON PROYECTOS.COMPANIA     = DEPENDENCIA.COMPANIA
			AND PROYECTOS.DEPENDENCIA = DEPENDENCIA.CODIGO
			LEFT JOIN RESPONSABLE
			ON PROYECTOS.COMPANIA     = RESPONSABLE.COMPANIA
			AND PROYECTOS.RESPONSABLE = RESPONSABLE.CEDULA
      LEFT JOIN TERCERO
      ON RESPONSABLE.COMPANIA = TERCERO.COMPANIA
      AND RESPONSABLE.CEDULA = TERCERO.NIT
      AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
   LEFT JOIN REGISTROFECHA
      ON PROYECTOS.COMPANIA = REGISTROFECHA.COMPANIA
      AND PROYECTOS.CODIGO = REGISTROFECHA.CODIGO
  INNER JOIN USUARIO
  ON PROYECTOS.CREATED_BY = USUARIO.CODIGO
WHERE PROYECTOS.COMPANIA = s$compania$s
AND PROYECTOS.CODIGO = 's$codigo$s'
