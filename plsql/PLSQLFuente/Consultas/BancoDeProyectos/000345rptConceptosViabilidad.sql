SELECT BP_CONCEPTOVIABILIDAD.COMPANIA, 
		BP_CONCEPTOVIABILIDAD.CODIGOTIPO, 
		BP_CONCEPTOVIABILIDAD.PROYECTO, 
		BP_CONCEPTOVIABILIDAD.CONCEPTOMUN, 
		BP_CONCEPTOVIABILIDAD.CONCEPTODPTO, 
		BP_CONCEPTOVIABILIDAD.CONCEPTOPAIS, 
		BP_TIPOCONCEPTOVIABILIDAD.TIPO, 
		BP_TIPOCONCEPTOVIABILIDAD.NOMBRE NOMBRECONCEPTO
FROM BP_CONCEPTOVIABILIDAD 
	INNER JOIN BP_TIPOCONCEPTOVIABILIDAD ON 
			BP_CONCEPTOVIABILIDAD.COMPANIA			= BP_TIPOCONCEPTOVIABILIDAD.COMPANIA
		AND BP_CONCEPTOVIABILIDAD.CODIGOTIPO		= BP_TIPOCONCEPTOVIABILIDAD.CODIGO
WHERE BP_CONCEPTOVIABILIDAD.COMPANIA				= s$compania$s
	AND BP_CONCEPTOVIABILIDAD.PROYECTO				= 's$proyecto$s'
	AND BP_TIPOCONCEPTOVIABILIDAD.TIPO				= 'CON'
ORDER BY BP_CONCEPTOVIABILIDAD.COMPANIA, 
	BP_CONCEPTOVIABILIDAD.CODIGOTIPO, 
	BP_CONCEPTOVIABILIDAD.PROYECTO
