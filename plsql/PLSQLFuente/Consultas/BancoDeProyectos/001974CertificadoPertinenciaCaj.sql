SELECT DISTINCT BPNOVEDADPROYECTO.COMPANIA
       ,BPNOVEDADPROYECTO.CODIGO
       ,TO_CHAR(BPNOVEDADPROYECTO.FECHA,'DD/MM/YYYY') AS FECHA 
       ,BPNOVEDADPROYECTO.OBJETO
       ,SUM(BPNOVEDADPROYECTO.VALORSOLICITADO) AS VALOR_SOLI
       ,DEPENDENCIA.CODIGO AS CODIGO_DEPENDENCIA
       ,DEPENDENCIA.NOMBRE AS NOMBRE_DEPENDENCIA
       ,BP_PLAN_INDICATIVO.NOMBRE_INDICADOR
       ,BP_PLAN_INDICATIVO.PREDECESOR
       ,PREGUNTAS_NOVEDAD.PREGUNTA
       ,PREGUNTAS_NOVEDAD.RESPUESTA AS RTA
       ,PROYECTOS.CODIGO AS CODIGO_PROYECTO
       ,PROYECTOS.NOMBREPROYECTO AS PROYECTO
       ,PROYECTOS.CODIGOBPIM AS CODIGO_BPIN
       ,TO_CHAR(SYSDATE,'DD')DIA
       ,TO_CHAR(SYSDATE, 'month')MES
       ,TO_CHAR(SYSDATE, 'YYYY') ANIO
       ,BP_PERTINENCIA_NOVEDAD.IMPACTO_CUAL
       ,BP_PERTINENCIA_NOVEDAD.IMPACTO_COMO
       ,BP_PERTINENCIA_NOVEDAD.IMPACTO_CUANTO
       ,BP_PERTINENCIA_NOVEDAD.RESULTADO_CUAL
       ,BP_PERTINENCIA_NOVEDAD.RESULTADO_COMO
       ,BP_PERTINENCIA_NOVEDAD.RESULTADO_CUANTO 
       ,BP_PERTINENCIA_NOVEDAD.RESULTADO_N_META 
       ,BP_PERTINENCIA_NOVEDAD.PRODUCTO_CUAL 
       ,BP_PERTINENCIA_NOVEDAD.PRODUCTO_COMO 
       ,BP_PERTINENCIA_NOVEDAD.PRODUCTO_CUANTO
       ,BP_PERTINENCIA_NOVEDAD.APPT
       ,BP_PERTINENCIA_NOVEDAD.APPT_CUAL 
       ,BP_PERTINENCIA_NOVEDAD.APPT_COMO 
       ,BP_PERTINENCIA_NOVEDAD.APPT_CUANTO
       ,BP_PERTINENCIA_NOVEDAD.APBOT
       ,BP_PERTINENCIA_NOVEDAD.APBOT_CUAL
       ,BP_PERTINENCIA_NOVEDAD.APBOT_COMO
       ,BP_PERTINENCIA_NOVEDAD.APBOT_CUANTO
 
FROM BPNOVEDADPROYECTO
INNER JOIN DEPENDENCIA 
ON BPNOVEDADPROYECTO.COMPANIA = DEPENDENCIA.COMPANIA 
AND BPNOVEDADPROYECTO.DEPENDENCIA = DEPENDENCIA.CODIGO
INNER JOIN BP_D_NOVEDADPROYECTO
ON BPNOVEDADPROYECTO.COMPANIA = BP_D_NOVEDADPROYECTO.COMPANIA
AND BPNOVEDADPROYECTO.TIPOT = BP_D_NOVEDADPROYECTO.TIPOT
AND BPNOVEDADPROYECTO.CLASET = BP_D_NOVEDADPROYECTO.CLASET
AND BPNOVEDADPROYECTO.CODIGO = BP_D_NOVEDADPROYECTO.NOVEDAD
AND BPNOVEDADPROYECTO.DEPENDENCIA = BP_D_NOVEDADPROYECTO.DEPENDENCIA
LEFT JOIN PREGUNTAS_NOVEDAD
ON BPNOVEDADPROYECTO.COMPANIA = PREGUNTAS_NOVEDAD.COMPANIA
AND BPNOVEDADPROYECTO.TIPOT   = PREGUNTAS_NOVEDAD.TIPONOVEDAD
AND BPNOVEDADPROYECTO.CLASET  = PREGUNTAS_NOVEDAD.CLASENOVEDAD
AND BPNOVEDADPROYECTO.CODIGO  = PREGUNTAS_NOVEDAD.NOVEDAD
AND BPNOVEDADPROYECTO.DEPENDENCIA = PREGUNTAS_NOVEDAD.DEPENDENCIA
INNER JOIN BP_PLAN_INDICATIVO
ON BP_D_NOVEDADPROYECTO.COMPANIA = BP_PLAN_INDICATIVO.COMPANIA
AND BP_D_NOVEDADPROYECTO.ID_META_PRODUCTO = BP_PLAN_INDICATIVO.ID
AND BP_D_NOVEDADPROYECTO.VIGENCIA_PLAN_INDICATIVO = BP_PLAN_INDICATIVO.VIGENCIA_INICIAL
INNER JOIN PROYECTOS
ON BP_D_NOVEDADPROYECTO.COMPANIA = PROYECTOS.COMPANIA
AND BP_D_NOVEDADPROYECTO.PROYECTO = PROYECTOS.CODIGO
INNER JOIN BP_PERTINENCIA_NOVEDAD
ON BPNOVEDADPROYECTO.COMPANIA = BP_PERTINENCIA_NOVEDAD.COMPANIA
AND BPNOVEDADPROYECTO.TIPOT = BP_PERTINENCIA_NOVEDAD.TIPONOVEDAD
AND BPNOVEDADPROYECTO.CLASET = BP_PERTINENCIA_NOVEDAD.CLASENOVEDAD
AND BPNOVEDADPROYECTO.CODIGO = BP_PERTINENCIA_NOVEDAD.NOVEDAD
AND BPNOVEDADPROYECTO.DEPENDENCIA = BP_PERTINENCIA_NOVEDAD.DEPENDENCIA
        WHERE BPNOVEDADPROYECTO.COMPANIA     = s$compania$s
    AND BPNOVEDADPROYECTO.TIPOT           = 's$tipot$s'
    AND BPNOVEDADPROYECTO.CLASET          = 's$claset$s'
    AND BPNOVEDADPROYECTO.CODIGO          = 's$consecutivo$s'
GROUP BY BPNOVEDADPROYECTO.COMPANIA
       ,BPNOVEDADPROYECTO.CODIGO
       ,BPNOVEDADPROYECTO.FECHA
       ,BPNOVEDADPROYECTO.OBJETO
       ,DEPENDENCIA.CODIGO 
       ,DEPENDENCIA.NOMBRE 
       ,BP_PLAN_INDICATIVO.NOMBRE_INDICADOR
       ,BP_PLAN_INDICATIVO.PREDECESOR
       ,PREGUNTAS_NOVEDAD.PREGUNTA
       ,PREGUNTAS_NOVEDAD.RESPUESTA
       ,PROYECTOS.CODIGO 
       ,PROYECTOS.NOMBREPROYECTO
       ,PROYECTOS.CODIGOBPIM
       ,BP_PERTINENCIA_NOVEDAD.IMPACTO_CUAL
       ,BP_PERTINENCIA_NOVEDAD.IMPACTO_COMO
       ,BP_PERTINENCIA_NOVEDAD.IMPACTO_CUANTO
       ,BP_PERTINENCIA_NOVEDAD.RESULTADO_CUAL
       ,BP_PERTINENCIA_NOVEDAD.RESULTADO_COMO
       ,BP_PERTINENCIA_NOVEDAD.RESULTADO_CUANTO 
       ,BP_PERTINENCIA_NOVEDAD.RESULTADO_N_META 
       ,BP_PERTINENCIA_NOVEDAD.PRODUCTO_CUAL 
       ,BP_PERTINENCIA_NOVEDAD.PRODUCTO_COMO 
       ,BP_PERTINENCIA_NOVEDAD.PRODUCTO_CUANTO
       ,BP_PERTINENCIA_NOVEDAD.APPT
       ,BP_PERTINENCIA_NOVEDAD.APPT_CUAL 
       ,BP_PERTINENCIA_NOVEDAD.APPT_COMO 
       ,BP_PERTINENCIA_NOVEDAD.APPT_CUANTO
       ,BP_PERTINENCIA_NOVEDAD.APBOT
       ,BP_PERTINENCIA_NOVEDAD.APBOT_CUAL
       ,BP_PERTINENCIA_NOVEDAD.APBOT_COMO
       ,BP_PERTINENCIA_NOVEDAD.APBOT_CUANTO
