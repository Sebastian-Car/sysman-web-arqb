SELECT BP_PROYECTO_PLAN_INDICATIVO.ID_PLAN_P,
       SUBSTR(BP_PROYECTO_PLAN_INDICATIVO.ID_PLAN_P,0,s$digitometres$s) METARESULT,
       BP_PLAN_INDICATIVO.DESCRIPCION METADESCRIP,
       SUBSTR(BP_PROYECTO_PLAN_INDICATIVO.ID_PLAN_P,0,s$digitometres$s) METAPROD,
       SUM(COSTOTOTAL) METAPRESUP,
       PROYECTOS.CANTIDAD ALCPROYECTO,
       BP_PLAN_INDICATIVO.META VALORPDM
  FROM PROYECTOS
 INNER JOIN BP_PROYECTO_PLAN_INDICATIVO
    ON PROYECTOS.COMPANIA = BP_PROYECTO_PLAN_INDICATIVO.COMPANIA
   AND PROYECTOS.CODIGO   = BP_PROYECTO_PLAN_INDICATIVO.PROYECTO
 INNER JOIN BP_PLAN_INDICATIVO
    ON BP_PROYECTO_PLAN_INDICATIVO.COMPANIA                             = BP_PLAN_INDICATIVO.COMPANIA
   AND SUBSTR(BP_PROYECTO_PLAN_INDICATIVO.ID_PLAN_P,0,s$digitometres$s) = BP_PLAN_INDICATIVO.ID
   AND BP_PROYECTO_PLAN_INDICATIVO.VIGENCIA_PLAN_P                      = BP_PLAN_INDICATIVO.VIGENCIA_INICIAL
 INNER JOIN COMPONENTES_ACTIVIDADES
    ON BP_PROYECTO_PLAN_INDICATIVO.COMPANIA       = COMPONENTES_ACTIVIDADES.COMPANIA
   AND BP_PROYECTO_PLAN_INDICATIVO.PROYECTO       = COMPONENTES_ACTIVIDADES.CODIGOPROYECTO
   AND BP_PROYECTO_PLAN_INDICATIVO.COMPONENTE     = COMPONENTES_ACTIVIDADES.COMPONENTE
   AND BP_PROYECTO_PLAN_INDICATIVO.TIPOCOMPONENTE = COMPONENTES_ACTIVIDADES.TIPOCOMPONENTE
   AND BP_PROYECTO_PLAN_INDICATIVO.ACTIVIDAD      = COMPONENTES_ACTIVIDADES.ACTIVIDAD
 WHERE PROYECTOS.COMPANIA   = s$compania$s
   AND PROYECTOS.CODIGO     = s$codigo$s
 GROUP BY SUBSTR(BP_PROYECTO_PLAN_INDICATIVO.ID_PLAN_P,0,s$digitometres$s),
       BP_PROYECTO_PLAN_INDICATIVO.ID_PLAN_P,
       BP_PLAN_INDICATIVO.DESCRIPCION ,
       PROYECTOS.CANTIDAD ,
       BP_PLAN_INDICATIVO.META  
