SELECT  
DISTINCT
	ORDENDECOMPRA_AUXILIAR.ORDENDECOMPRA,  
	ORDENDECOMPRA_AUXILIAR.AUXILIAR, 
	ORDENDECOMPRA.COMPONENTEBP,  
	COMPONENTES.NOMBRECOMPONENTE,  
	CASE WHEN EXTRACT (MONTH FROM FECHACREACION)>=0 AND EXTRACT (MONTH FROM FECHACREACION)<=3 THEN 1 ELSE 
		CASE WHEN EXTRACT(MONTH FROM FECHACREACION)>=4 AND EXTRACT (MONTH FROM FECHACREACION)<=6 THEN 2 ELSE 
			CASE WHEN EXTRACT(MONTH FROM FECHACREACION)>=7 AND EXTRACT(MONTH FROM FECHACREACION)<=9 THEN 3 ELSE 
				CASE WHEN EXTRACT(MONTH FROM FECHACREACION)>=10 AND EXTRACT(MONTH FROM FECHACREACION)<=12 THEN 4 ELSE 0 END END END END TRIMESTRE, 
	CASE WHEN EXTRACT (MONTH FROM FECHACREACION)>=0 AND EXTRACT (MONTH FROM FECHACREACION)<=3 THEN 'PRIMER  TRIMESTRE' ELSE 
		CASE WHEN EXTRACT(MONTH FROM FECHACREACION)>=4 AND EXTRACT (MONTH FROM FECHACREACION)<=6 THEN 'SEGUNDO  TRIMESTRE' ELSE 
			CASE WHEN EXTRACT(MONTH FROM FECHACREACION)>=7 AND EXTRACT(MONTH FROM FECHACREACION)<=9 THEN 'TERCER  TRIMESTRE' ELSE 
				CASE WHEN EXTRACT(MONTH FROM FECHACREACION)>=10 AND EXTRACT(MONTH FROM FECHACREACION)<=12 THEN 'CUARTO  TRIMESTRE' ELSE ' ' END END END END TRIMESTREN, 
	EXTRACT (YEAR FROM FECHACREACION) CODANO,  
	PROYECTOS.CODIGO,  
	PROYECTOS.NOMBREPROYECTO,  
	ORDENDECOMPRA_AUXILIAR.CLASEORDEN || ' - ' || ORDENDECOMPRA_AUXILIAR.ORDENDECOMPRA CONTRATO,  
	ORDENDECOMPRA.OBJETOCONTRATO,  
	ORDENDECOMPRA.DURACION,  
	ORDENDECOMPRA.VALORTOTAL,  
	ORDENDECOMPRA.VALORDELOSPAGOS,  
	ORDENDECOMPRA.VALORTOTAL-ORDENDECOMPRA.VALORDELOSPAGOS SALDOS,  
	TO_CHAR(ORDENDECOMPRA.FECHA,  
'DD/MM/YYYY') FECHA, 
	TO_CHAR(ORDENDECOMPRA.FECHAFINALIZACION,  
'DD/MM/YYYY') FECHAFINALIZACION, 
	ORDENDECOMPRA.INTERVENTOR,  
	V_ORDENDECOMPRA_RUBRO.RUBRONOMBRE RUBRO,  
	SUBSTR(ORDENDECOMPRA_AUXILIAR.ORDENDECOMPRA,3,2) || '-' || SUBSTR(ORDENDECOMPRA_AUXILIAR.ORDENDECOMPRA,5,4) PROYECTOAUX,  
	AUXILIAR.CODIGO CODIGO1,  
	AUXILIAR.CODIGOBP,  
	PROYECTOS.VIGENCIAINICIO 
FROM ORDENDECOMPRA  
	INNER JOIN V_ORDENDECOMPRA_RUBRO ON 
			ORDENDECOMPRA.COMPANIA					= V_ORDENDECOMPRA_RUBRO.COMPANIA 
		AND ORDENDECOMPRA.CLASEORDEN				= V_ORDENDECOMPRA_RUBRO.CLASEORDEN 
		AND ORDENDECOMPRA.NUMERO					= V_ORDENDECOMPRA_RUBRO.NUMERO 
	INNER JOIN ORDENDECOMPRA_AUXILIAR ON  
			ORDENDECOMPRA.COMPANIA					= ORDENDECOMPRA_AUXILIAR.COMPANIA 
		AND ORDENDECOMPRA.CLASEORDEN				= ORDENDECOMPRA_AUXILIAR.CLASEORDEN 
		AND ORDENDECOMPRA.NUMERO					= ORDENDECOMPRA_AUXILIAR.ORDENDECOMPRA  
	INNER JOIN AUXILIAR ON  
			ORDENDECOMPRA_AUXILIAR.COMPANIA			= AUXILIAR.COMPANIA 
		AND ORDENDECOMPRA_AUXILIAR.AUXILIAR			= AUXILIAR.CODIGO 
	INNER JOIN PROYECTOS ON  
			AUXILIAR.COMPANIA						= PROYECTOS.COMPANIA 
		AND AUXILIAR.CODIGOBP						= PROYECTOS.CODIGO 
	LEFT JOIN COMPONENTES ON  
			ORDENDECOMPRA.COMPANIA					= COMPONENTES.COMPANIA 
		AND ORDENDECOMPRA.COMPONENTEBP				= COMPONENTES.CODIGO 
WHERE ORDENDECOMPRA_AUXILIAR.COMPANIA				=s$compania$s  
	AND ORDENDECOMPRA_AUXILIAR.AUXILIAR				='s$proyecto$s'
