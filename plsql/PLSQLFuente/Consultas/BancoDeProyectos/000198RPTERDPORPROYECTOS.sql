SELECT  
   BPNOVEDADPROYECTO.TIPOT
  ,BPNOVEDADPROYECTO.CODIGO CODIGONOVEDAD
  ,PROYECTOS.CODIGO
  ,BP_D_NOVEDADPROYECTO.PROYECTO
  ,PROYECTOS.NOMBREPROYECTO
  ,COMPONENTES.NOMBRECOMPONENTE
  ,BP_ACTIVIDADES.NOMBRE NOMBREACTIVIDAD
  ,BPNOVEDADPROYECTO.VIGENCIA
  ,BPNOVEDADPROYECTO.FECHA
  ,BPNOVEDADPROYECTO.NUMERORADICADO
  ,BPNOVEDADPROYECTO.ESTADO
  ,BPNOVEDADPROYECTO.TIPOT_AFECTAR
  ,BPNOVEDADPROYECTO.DOCUMENTO_AFECTAR
  ,BPNOVEDADPROYECTO.DEPENDENCIA_AFECTAR
  ,BPNOVEDADPROYECTO.CLASET_AFECTAR
  ,BPNOVEDADPROYECTO.OBJETO
  ,BPNOVEDADPROYECTO.VOBOBP
  ,BPTIPONOVEDAD.CLASENOVEDAD
  ,CASE WHEN BP_D_NOVEDADPROYECTO.CONPROGRAMACION_C IN(0)
        THEN 'NO'
        ELSE 'SI'
   END CONPROGRAMACION_C    
FROM BPTIPONOVEDAD  
  INNER JOIN BPNOVEDADPROYECTO  
     ON BPTIPONOVEDAD.COMPANIA = BPNOVEDADPROYECTO.COMPANIA  
    AND BPTIPONOVEDAD.TIPOT    = BPNOVEDADPROYECTO.TIPOT   
    AND BPTIPONOVEDAD.CLASET   = BPNOVEDADPROYECTO.CLASET  
  INNER JOIN BP_D_NOVEDADPROYECTO  
     ON BPNOVEDADPROYECTO.COMPANIA    = BP_D_NOVEDADPROYECTO.COMPANIA  
    AND BPNOVEDADPROYECTO.TIPOT       = BP_D_NOVEDADPROYECTO.TIPOT  
    AND BPNOVEDADPROYECTO.CLASET      = BP_D_NOVEDADPROYECTO.CLASET  
    AND BPNOVEDADPROYECTO.CODIGO      = BP_D_NOVEDADPROYECTO.NOVEDAD  
    AND BPNOVEDADPROYECTO.DEPENDENCIA = BP_D_NOVEDADPROYECTO.DEPENDENCIA  
  INNER JOIN PROYECTOS  
     ON BP_D_NOVEDADPROYECTO.COMPANIA = PROYECTOS.COMPANIA  
    AND BP_D_NOVEDADPROYECTO.PROYECTO = PROYECTOS.CODIGO  
  INNER JOIN COMPONENTES  
     ON BP_D_NOVEDADPROYECTO.COMPANIA   = COMPONENTES.COMPANIA  
    AND BP_D_NOVEDADPROYECTO.PROYECTO   = COMPONENTES.CODIGOPROYECTO  
    AND BP_D_NOVEDADPROYECTO.COMPONENTE = COMPONENTES.CODIGO  
   LEFT JOIN BP_ACTIVIDADES  
     ON BP_D_NOVEDADPROYECTO.COMPANIA  = BP_ACTIVIDADES.COMPANIA  
    AND BP_D_NOVEDADPROYECTO.ACTIVIDAD = BP_ACTIVIDADES.CODIGO  
WHERE BPTIPONOVEDAD.COMPANIA = s$compania$s
  AND BP_D_NOVEDADPROYECTO.PROYECTO BETWEEN s$proyInicial$s AND s$proyFinal$s 
  AND BPTIPONOVEDAD.CLASENOVEDAD IN('E')
