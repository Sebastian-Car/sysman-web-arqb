WITH INVERSION_NIVEL AS (
  SELECT 
     R.NIVEL
    ,R.DESCRIPCION
    ,(SUM(S.REG_CONTRACT    ) + 
      SUM(S.MODIF_REG_CONT  ) + 
      SUM(S.REG_NO_CONTRACT ) + 
      SUM(S.MODIF_REG_NOCONT)) TOTALCOMPROMETIDO
  FROM BP_RUBRO_INVERSION_DET R 
    INNER JOIN V_PLAN_PRESUPUESTAL P 
       ON R.COMPANIA = P.COMPANIA
      AND R.VIGENCIA = P.ANO
      AND R.RUBRO    = P.ID
     LEFT JOIN V_SALDO_PLAN_PPTAL S 
       ON P.COMPANIA = S.COMPANIA
      AND P.ANO      = S.ANO
      AND P.ID       = S.ID
  WHERE R.COMPANIA = s$compania$s  
    AND R.VIGENCIA BETWEEN s$anioInicial$s AND s$anioFinal$s 
    AND R.NIVEL    = s$nivel$s
  GROUP BY 
     R.NIVEL
    ,R.DESCRIPCION
    ,S.COMPANIA
    ,S.ANO
    ,S.ID
    ,P.NATURALEZA
)
SELECT 
   NIVEL
  ,DESCRIPCION
  ,TOTALCOMPROMETIDO
  ,CASE WHEN SUM(TOTALCOMPROMETIDO) OVER() IN (0) 
        THEN 0
        ELSE TOTALCOMPROMETIDO/SUM(TOTALCOMPROMETIDO) OVER()
   END TOTAL
FROM INVERSION_NIVEL
