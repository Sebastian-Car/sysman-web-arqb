SELECT 
   DEPENDENCIA.CODIGO
  ,DEPENDENCIA.NOMBRE NOMBREDEPENDENCIA
  ,RESPONSABLE.CEDULA
  ,TERCERO.NOMBRE     NOMBRERESPONSABLE
  ,RESPONSABLE.CARGO
  ,RESPONSABLE.COMPANIA
  ,RESPONSABLE.CEDULA
FROM DEPENDENCIA
  INNER JOIN DEPENDENCIA_RESPONSABLE
     ON DEPENDENCIA.COMPANIA = DEPENDENCIA_RESPONSABLE.COMPANIA
    AND DEPENDENCIA.CODIGO   = DEPENDENCIA_RESPONSABLE.DEPENDENCIA
  INNER JOIN RESPONSABLE
     ON DEPENDENCIA_RESPONSABLE.COMPANIA    = RESPONSABLE.COMPANIA
    AND DEPENDENCIA_RESPONSABLE.RESPONSABLE = RESPONSABLE.CEDULA
  INNER JOIN TERCERO
     ON RESPONSABLE.COMPANIA = TERCERO.COMPANIA
    AND RESPONSABLE.CEDULA   = TERCERO.NIT
    AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
WHERE DEPENDENCIA.COMPANIA = s$compania$s
AND DEPENDENCIA.CODIGO BETWEEN s$dependenciaInicial$s AND s$dependenciaFinal$s
GROUP BY 
   DEPENDENCIA.CODIGO
  ,DEPENDENCIA.NOMBRE
  ,TERCERO.NOMBRE
  ,RESPONSABLE.CARGO
  ,RESPONSABLE.COMPANIA
  ,RESPONSABLE.CEDULA
  ,RESPONSABLE.CEDULA
