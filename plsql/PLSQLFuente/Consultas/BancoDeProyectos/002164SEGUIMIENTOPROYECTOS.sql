SELECT DISTINCT
PROYECTOS.CODIGOBPIM,
PROYECTOS.NOMBREPROYECTO,
PROYECTOS.VIGENCIAINICIO, 
PROYECTOS.VIGENCIAFIN,
COMPONENTES_ACTIVIDADES.VALOREJECUTADO,
COMPONENTES_ACTIVIDADES.PORCEJECUTADO,
componentes.nombrecomponente AS PRODUCTO,
COMPONENTES_ACTIVIDADES.NOMBREACTIVIDAD
FROM PROYECTOS   
INNER JOIN COMPONENTES_ACTIVIDADES 
ON PROYECTOS.COMPANIA=COMPONENTES_ACTIVIDADES.COMPANIA
AND PROYECTOS.CODIGO=COMPONENTES_ACTIVIDADES.CODIGOPROYECTO
LEFT JOIN COMPONENTES 
		ON COMPONENTES_ACTIVIDADES.COMPANIA = COMPONENTES.COMPANIA
		AND COMPONENTES_ACTIVIDADES.CODIGOPROYECTO = COMPONENTES.CODIGOPROYECTO
		AND COMPONENTES_ACTIVIDADES.COMPONENTE = COMPONENTES.CODIGO
		AND COMPONENTES_ACTIVIDADES.TIPOCOMPONENTE = COMPONENTES.TIPOCOMPONENTE
WHERE PROYECTOS.COMPANIA = s$compania$s AND PROYECTOS.CODIGO BETWEEN 's$proyectoInicial$s' AND 's$proyectoFinal$s'
ORDER BY componentes.nombrecomponente