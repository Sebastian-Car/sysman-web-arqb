SELECT 
    INDICADORES.CODIGOTIPO, 
    TIPOSINDICADOR.NOMBRE  ASOCIACION, 
    INDICADORES.CODIGO, 
    INDICADORES.NOMBRE  NOMBRE, 
    INDICADORES.DESCRIPCION,       
    INDICADORES.CODIGOUNIDAD, 
    INDICADORES.FORMULA, 
    INDICADORES.CONVENCIONES 
FROM 
    INDICADORES 
        INNER JOIN TIPOSINDICADOR 
        ON 
             INDICADORES.COMPANIA = TIPOSINDICADOR.COMPANIA                 AND 
           INDICADORES.CODIGOTIPO = TIPOSINDICADOR.CODIGO 
WHERE 
    INDICADORES.COMPANIA =  s$compania$s
