SELECT CASE BPTIPONOVEDAD.AFECTAPROGRAMACION WHEN 0 THEN ' ' WHEN -1 THEN 'X' END     AFECTAPROGRAMACION ,  
      BPCLASENOVEDADP.NOMBRE CLASENOVEDAD,  
      (CASE WHEN BPTIPONOVEDAD.CLASET = 'B' THEN 'Banco de proyectos' ELSE  
      (CASE WHEN BPTIPONOVEDAD.CLASET = 'C' THEN 'Contabilidad' ELSE 
      (CASE WHEN BPTIPONOVEDAD.CLASET = 'P' THEN 'Presupuesto' ELSE 
      (CASE WHEN BPTIPONOVEDAD.CLASET = 'A' THEN 'Almacén' ELSE 
      (CASE WHEN BPTIPONOVEDAD.CLASET = 'N' THEN 'Contratos'  
       END)  
       END) 
       END) 
       END) 
       END) CLASET, 
      BPTIPONOVEDAD.NOMBRE,  
      (CASE WHEN BPTIPONOVEDAD.TAUTOMATICA = 'A' THEN 'Automática' ELSE  
      (CASE WHEN BPTIPONOVEDAD.TAUTOMATICA = 'M' THEN  'Manual' END) 
      END) TAUTOMATICA, 
      BPTIPONOVEDAD.TIPOFORMATO, 
      (CASE WHEN BPTIPONOVEDAD.TIPOMODELO = 'R' THEN 'Reporte' ELSE  
      (CASE WHEN BPTIPONOVEDAD.TIPOMODELO = 'C' THEN 'Contrato' END)  
      END) TIPOMODELO,  
      BPTIPONOVEDAD.TIPOT 
FROM BPTIPONOVEDAD INNER JOIN BPCLASENOVEDADP 
 ON  BPTIPONOVEDAD.CLASENOVEDAD = BPCLASENOVEDADP.CODIGO 
WHERE BPTIPONOVEDAD.COMPANIA= s$compania$s 
ORDER BY BPTIPONOVEDAD.NOMBRE
