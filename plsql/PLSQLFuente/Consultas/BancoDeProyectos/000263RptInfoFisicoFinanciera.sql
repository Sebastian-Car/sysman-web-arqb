SELECT DISTINCT 
    PROYECTOS.COMPANIA, 
    PROYECTOS.CODIGO, 
    PROGRAMACION.CODIGOPROYECTO, 
    PROYECTOS.NOMBREPROYECTO, 
    TO_CHAR(PROYECTOS.FECHAREGISTRO, 'DD/MM/YYYY') FECHAREGISTRO, 
    PROGRAMACION.VIGENCIA, 
    PROGRAMACION.REPROGRAMADO, 
    CASE WHEN 's$vigencia$s' = 'TODAS' THEN 's$vigencia$s' ELSE TO_CHAR(PROGRAMACION.VIGENCIA) END  V, 
    CASE WHEN 's$vigencia$s' = 'TODOS' THEN NVL(VIGENCIAINICIO,0) || ' A ' || NVL(VIGENCIAFIN, 0) ELSE TO_CHAR(PROGRAMACION.VIGENCIA) END  VIGENCIANOMBRE, 
    CASE PROYECTOS.ESTADOACTUAL WHEN  'E' THEN 'Ejecución' 
                                WHEN 'S' THEN 'Sin ejecutar' 
                                WHEN 'P' THEN 'Ejecutado parcialmente' 
                                WHEN 'T' THEN 'Ejecutado' ELSE 'Sin estado' END ESTADOACTUAL, 
    CASE PROYECTOS.ENVIADONACION WHEN 0 THEN  '' ELSE 'X' END ENVIADONACION, 
    CASE PROYECTOS.ENVIADODPTO WHEN  0 THEN '' ELSE 'X' END ENVIADODPTO, 
    CASE PROYECTOS.ENVIADOOTRAENTIDAD WHEN  0 THEN '' ELSE 'X' END ENVIADOOTRAENTIDAD, 
    PROYECTOS.CODIGOBPIM, 
    PROGRAMACION.TIPOCOMPONENTE, 
    PROGRAMACION.CODIGOACTIVIDAD, 
    PROGRAMACION.TIPOESTADO, 
    PROGRAMACION.CODIGO  COD_PROGRA 
FROM PROYECTOS LEFT JOIN PROGRAMACION ON (PROYECTOS.COMPANIA = PROGRAMACION.COMPANIA) 
	AND (PROYECTOS.CODIGO = PROGRAMACION.CODIGOPROYECTO) 
WHERE PROYECTOS.COMPANIA =  s$compania$s 
	AND PROYECTOS.CODIGO BETWEEN 's$proyectoInicial$s' AND 's$proyectoFin$s' 
	AND PROGRAMACION.REPROGRAMADO = 0
	AND PROGRAMACION.VIGENCIA = CASE WHEN 's$vigencia$s' = 'TODAS' THEN TO_CHAR(PROGRAMACION.VIGENCIA) ELSE 's$vigencia$s' END
	AND PROYECTOS.ESTADOACTUAL = CASE WHEN 's$estadoActual$s' = 'X' THEN TO_CHAR(PROYECTOS.ESTADOACTUAL) ELSE 's$estadoActual$s' END
	AND PROYECTOS.ENTIDADPROPONENTE = CASE WHEN 's$entidad$s' = 'X' THEN TO_CHAR(PROYECTOS.ENTIDADPROPONENTE) ELSE 's$entidad$s' END
	s$enviado$s
