SELECT  BP_PROYECTOSRUBROS.PROYECTO,
        BP_PROYECTOSRUBROS.VIGENCIA,
        BP_PROYECTOSRUBROS.RUBROPPTALES,
        V_PLAN_PRESUPUESTAL.NOMBRE NOMBRE_RUBRO,
        FUENTE_RECURSOS.NOMBRE NOMBRE_FUENTE
FROM (BP_PROYECTOSRUBROS LEFT JOIN FUENTE_RECURSOS 
        ON BP_PROYECTOSRUBROS.COMPANIA = FUENTE_RECURSOS.COMPANIA
          AND BP_PROYECTOSRUBROS.FUENTERECURSOSRUBRO = FUENTE_RECURSOS.CODIGO 
      )LEFT JOIN V_PLAN_PRESUPUESTAL 
        ON BP_PROYECTOSRUBROS.COMPANIA = V_PLAN_PRESUPUESTAL.COMPANIA 
          AND BP_PROYECTOSRUBROS.VIGENCIA = V_PLAN_PRESUPUESTAL.ANO 
          AND BP_PROYECTOSRUBROS.RUBROPPTALES = V_PLAN_PRESUPUESTAL.ID
WHERE BP_PROYECTOSRUBROS.COMPANIA = $P{PR_COMPANIA} 
    AND BP_PROYECTOSRUBROS.PROYECTO = $P{PR_PROYECTO} 
GROUP BY BP_PROYECTOSRUBROS.PROYECTO,
         BP_PROYECTOSRUBROS.VIGENCIA,
         BP_PROYECTOSRUBROS.RUBROPPTALES,
         V_PLAN_PRESUPUESTAL.NOMBRE,
         FUENTE_RECURSOS.NOMBRE
