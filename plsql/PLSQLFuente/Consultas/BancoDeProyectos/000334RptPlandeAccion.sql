WITH PROYECTOS_RUBROS AS (
SELECT BP_PROYECTOSRUBROS.COMPANIA, 
	BP_PROYECTOSRUBROS.PROYECTO PROYECTO,
	MIN(BP_PROYECTOSRUBROS.DIMENSION) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) DIMENSION,
	MIN(BP_PROYECTOSRUBROS.SECTOR) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) SECTOR,
	MIN(BP_PROYECTOSRUBROS.PROGRAMA) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) PROGRAMA,
	MIN(BP_PROYECTOSRUBROS.SUBPROGRAMA) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) SUBPROGRAMA,
	MIN(PLAN_PPTAL.ANO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) DIM,
	MIN(PLAN_PPTAL_1.ANO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) SEC,
	MIN(PLAN_PPTAL_2.ANO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) PROG,
	MIN(PLAN_PPTAL_3.ANO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) SUBPROG,
	MIN(PLAN_PPTAL.NOMBRE) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) NOMBREDIMENSION,
	MIN(PLAN_PPTAL_1.NOMBRE) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) NEMBRESECTOR,
	MIN(PLAN_PPTAL_2.NOMBRE) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) NOMBREPROGRAMA,
	MIN(PLAN_PPTAL_3.NOMBRE) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) NOMBRESUBPROGRAMA
FROM BP_PROYECTOSRUBROS INNER JOIN (SELECT 
			  MIN(PLAN_PRESUPUESTAL.COMPANIA) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) PCOMPANIA, 
			  PLAN_PRESUPUESTAL.ANO ANO, 
			  PLAN_PRESUPUESTAL.ID ID, 
			  MAX(PLAN_PRESUPUESTAL.NOMBRE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NOMBRE
			  FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
			  GROUP BY PLAN_PRESUPUESTAL.ANO, PLAN_PRESUPUESTAL.ID
			  ORDER BY MIN(PLAN_PRESUPUESTAL.COMPANIA) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM), PLAN_PRESUPUESTAL.ANO, PLAN_PRESUPUESTAL.ID) PLAN_PPTAL 
	ON BP_PROYECTOSRUBROS.COMPANIA = PLAN_PPTAL.PCOMPANIA
    AND BP_PROYECTOSRUBROS.DIMENSION = PLAN_PPTAL.ID
    AND BP_PROYECTOSRUBROS.VIGENCIA = PLAN_PPTAL.ANO
  INNER JOIN (SELECT 
		  MIN(PLAN_PRESUPUESTAL.COMPANIA) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) PCOMPANIA, 
		  PLAN_PRESUPUESTAL.ANO ANO, 
		  PLAN_PRESUPUESTAL.ID ID, 
		  MAX(PLAN_PRESUPUESTAL.NOMBRE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NOMBRE
		  FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
		  GROUP BY PLAN_PRESUPUESTAL.ANO, PLAN_PRESUPUESTAL.ID
		  ORDER BY MIN(PLAN_PRESUPUESTAL.COMPANIA) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM), PLAN_PRESUPUESTAL.ANO, PLAN_PRESUPUESTAL.ID) PLAN_PPTAL_1 
	ON BP_PROYECTOSRUBROS.COMPANIA = PLAN_PPTAL_1.PCOMPANIA
    AND BP_PROYECTOSRUBROS.SECTOR = PLAN_PPTAL_1.ID
    AND BP_PROYECTOSRUBROS.VIGENCIA = PLAN_PPTAL_1.ANO
  INNER JOIN (SELECT 
      MIN(PLAN_PRESUPUESTAL.COMPANIA) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) PCOMPANIA, 
      PLAN_PRESUPUESTAL.ANO ANO, 
      PLAN_PRESUPUESTAL.ID ID, 
      MAX(PLAN_PRESUPUESTAL.NOMBRE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NOMBRE
      FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
      GROUP BY PLAN_PRESUPUESTAL.ANO, PLAN_PRESUPUESTAL.ID
      ORDER BY MIN(PLAN_PRESUPUESTAL.COMPANIA) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM), PLAN_PRESUPUESTAL.ANO, PLAN_PRESUPUESTAL.ID) PLAN_PPTAL_2 
	 ON BP_PROYECTOSRUBROS.COMPANIA = PLAN_PPTAL_2.PCOMPANIA 
    AND BP_PROYECTOSRUBROS.PROGRAMA = PLAN_PPTAL_2.ID
    AND BP_PROYECTOSRUBROS.VIGENCIA = PLAN_PPTAL_2.ANO
  INNER JOIN (SELECT 
      MIN(PLAN_PRESUPUESTAL.COMPANIA) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) PCOMPANIA, 
      PLAN_PRESUPUESTAL.ANO ANO, 
      PLAN_PRESUPUESTAL.ID ID, 
      MAX(PLAN_PRESUPUESTAL.NOMBRE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NOMBRE
      FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
      GROUP BY PLAN_PRESUPUESTAL.ANO, PLAN_PRESUPUESTAL.ID
      ORDER BY MIN(PLAN_PRESUPUESTAL.COMPANIA) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM), PLAN_PRESUPUESTAL.ANO, PLAN_PRESUPUESTAL.ID) PLAN_PPTAL_3 
	  ON BP_PROYECTOSRUBROS.COMPANIA = PLAN_PPTAL_3.PCOMPANIA
    AND BP_PROYECTOSRUBROS.SUBPROGRAMA = PLAN_PPTAL_3.ID
    AND BP_PROYECTOSRUBROS.VIGENCIA = PLAN_PPTAL_3.ANO
WHERE BP_PROYECTOSRUBROS.COMPANIA = s$compania$s
GROUP BY BP_PROYECTOSRUBROS.COMPANIA, BP_PROYECTOSRUBROS.PROYECTO
ORDER BY BP_PROYECTOSRUBROS.PROYECTO
)
SELECT PROYECTOS.COMPANIA, 
		PROYECTOS.CODIGO, 
		NVL(PROYECTOS.CODIGO,0) CON1, 
		CASE WHEN 's$dependencia$s'='X' THEN 's$dependencia$s' ELSE PROYECTOS.DEPENDENCIA END DEP, 
		PROYECTOS.VALORPROGRAMADO, 
		PROYECTOS.CODIGOBPIM, 
		PROYECTOS.NOMBREPROYECTO, 
		PROYECTOS.DEPENDENCIA, 
		PROYECTOS.VALORTOTAL, 
		CASE PROYECTOS.ESTADOACTUAL WHEN 'E' THEN 'Ejecución' WHEN 'S' THEN 'Sin ejecutar' WHEN 'P' THEN 'Ejecutado parcialmente' WHEN 'T' THEN 'Ejecutado' ELSE 'Sin estado' END ESTADOACTUAL, 
		TO_CHAR(PROYECTOS.FECHAREGISTRO, 'dd/MM/YYYY') FECHAREGISTRO, 
		NVL(VIGENCIAINICIO,EXTRACT(YEAR FROM SYSDATE)) ANNO1, 
		PROYECTOS.VIGENCIAINICIO, 
		DEPENDENCIA.NOMBRE AS NOMBRESECRETARIA, 
		TERCERO.NOMBRE AS NOMBRERESPONSABLE, 
		EXTRACT (YEAR FROM FECHAREGISTRO) ANOPROYECTO, 
		PROYECTOS_RUBROS.DIM, 
		PROYECTOS_RUBROS.SEC, 
		PROYECTOS_RUBROS.PROG, 
		PROYECTOS_RUBROS.SUBPROG, 
		PROYECTOS_RUBROS.NOMBREDIMENSION, 
		PROYECTOS_RUBROS.NEMBRESECTOR, 
		PROYECTOS_RUBROS.NOMBREPROGRAMA, 
		PROYECTOS_RUBROS.NOMBRESUBPROGRAMA, 
		'2009' V1
FROM PROYECTOS 
  LEFT JOIN DEPENDENCIA ON PROYECTOS.COMPANIA = DEPENDENCIA.COMPANIA
    AND PROYECTOS.DEPENDENCIA = DEPENDENCIA.CODIGO 
  LEFT JOIN RESPONSABLE ON PROYECTOS.COMPANIA = RESPONSABLE.COMPANIA 
    AND PROYECTOS.RESPONSABLE = RESPONSABLE.CEDULA
	AND PROYECTOS.SUCURSAL = RESPONSABLE.SUCURSAL
  INNER JOIN TERCERO ON RESPONSABLE.COMPANIA = TERCERO.COMPANIA
	AND RESPONSABLE.CEDULA = TERCERO.NIT
    AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
  LEFT JOIN PROYECTOS_RUBROS ON PROYECTOS.COMPANIA = PROYECTOS_RUBROS.COMPANIA
    AND PROYECTOS.CODIGO = PROYECTOS_RUBROS.PROYECTO
WHERE PROYECTOS.COMPANIA = s$compania$s
  AND PROYECTOS.CODIGO BETWEEN 's$proyectoInicial$s' AND 's$proyectoFin$s' 
  AND PROYECTOS.DEPENDENCIA= CASE WHEN 's$dependencia$s' = 'X' THEN TO_CHAR(PROYECTOS.DEPENDENCIA) ELSE 's$dependencia$s' END
 AND PROYECTOS.VIGENCIAINICIO = CASE WHEN 's$vigencia$s' = 'TODAS' THEN TO_CHAR(PROYECTOS.VIGENCIAINICIO) ELSE 's$vigencia$s' END
ORDER BY PROYECTOS.CODIGO
