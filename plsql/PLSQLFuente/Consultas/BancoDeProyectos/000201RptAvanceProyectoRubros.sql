SELECT  
	TEMP.COMPANIA,  
	BP_PROYECTOSRUBROS.PROYECTO,  
	TEMP.ID,  
	TEMP.NOMBREPLAN,  
	SUM(APROPIACION_DEBITO-APROPIACION_CREDITO+ADICION-REDUCCION+TRASLADO_DEBITO-TRASLADO_CREDITO) APROPIADO,  
	SUM(CASE WHEN NATURALEZA='D' THEN EJE_PPT_DEBITO-EJE_PPT_CREDITO ELSE EJE_PPT_CREDITO-EJE_PPT_DEBITO END) EJECUCIONPPT,  
	SUM(REINTEGRO) REINTEGRO,  
	SUM(CASE WHEN NATURALEZA='D' THEN EJE_PPT_DEBITO-EJE_PPT_CREDITO ELSE EJE_PPT_CREDITO-EJE_PPT_DEBITO END)+SUM(REINTEGRO) PAGOS,  
	TEMP.ANO 
FROM BP_PROYECTOSRUBROS  
	INNER JOIN (SELECT S.COMPANIA, P.ID, P.NOMBRE NOMBREPLAN,S.ANO, P.NOMBRE, S.MES, P.MOVIMIENTO, REINTEGRO, EJE_PPT_DEBITO,  
              EJE_PPT_CREDITO, NATURALEZA, TRASLADO_CREDITO,TRASLADO_DEBITO, REDUCCION, ADICION, APROPIACION_CREDITO, APROPIACION_DEBITO 
              FROM V_PLAN_PRESUPUESTAL P  
                INNER JOIN V_SALDO_PLAN_PPTAL S ON  
                    P.COMPANIA                  = S.COMPANIA  
                  AND P.ID                          = S.ID  
                  AND P.ANO                       = S.ANO) TEMP ON  
        BP_PROYECTOSRUBROS.COMPANIA               = TEMP.COMPANIA  
			AND BP_PROYECTOSRUBROS.RUBROPPTALES         = TEMP.ID 
			AND BP_PROYECTOSRUBROS.VIGENCIA             = TEMP.ANO  
WHERE TEMP.COMPANIA                               = $P{PR_COMPANIA} 
  AND BP_PROYECTOSRUBROS.PROYECTO                 = $P{PR_PROYECTO} 
  AND TEMP.MOVIMIENTO                             <>0  
	AND TEMP.MES                                    <= EXTRACT(MONTH FROM SYSDATE) 
  AND  TEMP.ANO                                   =EXTRACT(YEAR FROM SYSDATE) 
GROUP BY TEMP.COMPANIA,  
  BP_PROYECTOSRUBROS.PROYECTO,  
  TEMP.ID,  
  TEMP.ANO,  
  TEMP.NOMBREPLAN
