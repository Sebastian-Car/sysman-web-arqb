SELECT ITO.CODIGORUTA,
	ITO.CONSECUTIVO,
  	CASE ITO.TIPO_OPERACION 
    	WHEN '001' THEN 'SUSPENSION' 
    	WHEN '002' THEN 'CORTE'
    	WHEN '003' THEN 'RECONEXION' 
    	WHEN '004' THEN 'REINSTALACION'
  	END TIPO_OPERACION, 
	ITO.AFORADOR,
	ITO.DESCRIPCION,
	ITO.LECTURA,
	M.CODIGO MEDIDOR,
	ITO.SELLO_RETIRADO,
	ITO.SELLO_INSTALADO,
	ITO.ESTADO,
	NVL(U.PRIMERAPELLIDO,' ')||' '||NVL(U.SEGUNDOAPELLIDO,' ')||' '||NVL(U.NOMBRES,' ') NOMBRES,
	ITO.HORAEJECUCION,
	US.NOMBRE USO,
	E.NOMBRE ESTRATO,
	U.DIRTECNICA,
	U.PERIODOSATRASO PERATRASO,
	ITO.OBSERVACIONES 
FROM SP_USUARIO U
	INNER JOIN SP_INFO_TIPO_OPERACION ITO
		ON U.COMPANIA 	 = ITO.COMPANIA
		AND U.CICLO 	 = ITO.CICLO
		AND U.CODIGORUTA = ITO.CODIGORUTA
  	INNER JOIN SP_MEDIDOR M
    	ON U.COMPANIA   = M.COMPANIA
       AND U.CICLO      = M.CICLO
       AND U.CODIGORUTA = M.CODIGORUTA
    INNER JOIN SP_USOS US
    	ON U.COMPANIA = US.COMPANIA
    	AND U.USO 	  = US.CODIGO
    INNER JOIN SP_ESTRATOS E
    	ON U.COMPANIA = E.COMPANIA
        AND U.ESTRATO = E.CODIGO
      	AND U.USO 	  = E.USO
WHERE U.COMPANIA=s$compania$s
	s$condicionCiclo$s
	s$condicionTipoOperacion$s
	s$condicionFechaEjecucion$s
	s$condicionHora$s
	s$condicionEstado$s
ORDER BY ITO.CICLO, 
	ITO.CODIGORUTA, 
	ITO.CONSECUTIVO
