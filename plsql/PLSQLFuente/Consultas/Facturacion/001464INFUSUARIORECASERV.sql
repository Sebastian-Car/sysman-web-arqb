WITH USUARIOS AS 
(
  SELECT COMPANIA,
        CICLO,
        CODIGORUTA,
        ANO,
        PERIODO,
        BANCOPERPROCESO     AS BANCO,
        FECHAPAGOPERPROCESO AS FECHA_PAGO
  FROM SP_USUARIO USUARIO
  WHERE COMPANIA = s$compania$s
    AND FECHAPAGOPERPROCESO BETWEEN s$fechaInicial$s AND s$fechaFinal$s 
  UNION
  SELECT COMPANIA,
        CICLO,
        CODIGORUTA,
        ANO,
        PERIODO,
        BANCO_PAGO AS BANCO,
        FECHA_PAGO
  FROM SP_HISTORIA_EXTERNA HISTORIA_EXTERNA
 WHERE COMPANIA = s$compania$s
   AND FECHA_PAGO BETWEEN s$fechaInicial$s AND s$fechaFinal$s 
)
SELECT 
  USUARIOS.CODIGORUTA, 
  USUARIO_1.CODIGOINTERNO, 
  USUARIO_1.CODIGO_EXTERNO, 
  USUARIOS.FECHA_PAGO,
  USUARIOS.BANCO                            AS BANCO_PAGO, 
  BANCOS.NOMBRE                             AS NOMBRE_BANCO, 
  NVL(HISTORIA_EXTERNA.VALORASEO,0)         AS TERCERIZADO, 
  NVL(USUARIO.TOTFACTURAPERACTUAL,0)        AS TOTFACTURAPERACTUAL, 
  NVL(HISTORIA_EXTERNA.VALORASEO,0) + 
  NVL(USUARIO.TOTFACTURAPERACTUAL,0)        AS GRANTOTAL, 
  NVL(USUARIO_1.EMPRESAASEOEXT,' ')         AS ID_EMPRESA, 
  NVL(EMPRESAS_TERCERIZA.NOMBRE,' ')        AS EMPRESA, 
  USUARIO_1.PRIMERAPELLIDO  || ' ' || 
  USUARIO_1.SEGUNDOAPELLIDO || ' ' || 
  USUARIO_1.NOMBRES                         AS SUSCRIPTOR 
FROM USUARIOS
LEFT JOIN SP_USUARIO USUARIO
     ON USUARIOS.FECHA_PAGO  = USUARIO.FECHAPAGOPERPROCESO
    AND USUARIOS.BANCO      = USUARIO.BANCOPERPROCESO
    AND USUARIOS.PERIODO    = USUARIO.PERIODO
    AND USUARIOS.ANO        = USUARIO.ANO
    AND USUARIOS.CODIGORUTA = USUARIO.CODIGORUTA
    AND USUARIOS.CICLO      = USUARIO.CICLO
    AND USUARIOS.COMPANIA   = USUARIO.COMPANIA
LEFT JOIN SP_HISTORIA_EXTERNA HISTORIA_EXTERNA
     ON USUARIOS.FECHA_PAGO = HISTORIA_EXTERNA.FECHA_PAGO
    AND USUARIOS.BANCO      = HISTORIA_EXTERNA.BANCO_PAGO
    AND USUARIOS.PERIODO    = HISTORIA_EXTERNA.PERIODO
    AND USUARIOS.ANO        = HISTORIA_EXTERNA.ANO
    AND USUARIOS.CODIGORUTA = HISTORIA_EXTERNA.CODIGORUTA
    AND USUARIOS.CICLO      = HISTORIA_EXTERNA.CICLO
    AND USUARIOS.COMPANIA   = HISTORIA_EXTERNA.COMPANIA
INNER JOIN SP_USUARIO USUARIO_1
     ON USUARIOS.CODIGORUTA = USUARIO_1.CODIGORUTA
    AND USUARIOS.CICLO      = USUARIO_1.CICLO
    AND USUARIOS.COMPANIA   = USUARIO_1.COMPANIA    
LEFT JOIN SP_EMPRESAS_TERCERIZA EMPRESAS_TERCERIZA
     ON USUARIO_1.EMPRESAASEOEXT = EMPRESAS_TERCERIZA.ID
    AND USUARIO_1.COMPANIA       = EMPRESAS_TERCERIZA.COMPANIA
INNER JOIN SP_BANCOS BANCOS
     ON USUARIOS.BANCO     = BANCOS.CODIGO
    AND USUARIOS.COMPANIA  = BANCOS.COMPANIA
