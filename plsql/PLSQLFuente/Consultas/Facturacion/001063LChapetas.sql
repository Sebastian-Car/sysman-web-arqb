SELECT COMPANIA, 
	CICLO, 
	CODIGORUTA,
 	PRIMERAPELLIDO,
  	SEGUNDOAPELLIDO, 
  	NOMBRES, 
  	DIRTECNICA,
  	TO_CHAR(FECHARENTRADANUEVOUSUARIO,'DD/MM/YYYY') FECHARENTRADANUEVOUSUARIO
FROM SP_USUARIO 
WHERE COMPANIA 	 =s$compania$s
	AND CICLO    =s$ciclo$s
    AND CHAPETAS NOT IN (0)
ORDER BY COMPANIA, 
	CICLO, 
	CODIGORUTA
