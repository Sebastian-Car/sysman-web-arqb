SELECT 
  SP_USUARIO.CICLO,
  SP_USUARIO.ANO,
  PCK_SYSMAN_UTL.FC_NOMBRE_MES(SP_USUARIO.PERIODO) PERIODO,
  SP_USUARIO.CODIGORUTA,
  SP_USUARIO.CODIGOINTERNO,
  SP_USUARIO.PERIODOSATRASO,
  SP_USUARIO.LECTURA,
  SP_USUARIO.LECTURA1,
  SP_USUARIO.LECTURA2,
  SP_USUARIO.LECTURA3,
  SP_USUARIO.LECTURA4,
  SP_USUARIO.LECTURA5,
  SP_USUARIO.LECTURA6,
  SP_USUARIO.CONSUMO,
  SP_USUARIO.CONSUMOPROM,
  DECODE(SP_USUARIO.FIMM,'F','F-Planos en la entidad','P','P - Usuario en Terreno', NULL , 'Normal') FIMM
FROM SP_USUARIO
WHERE SP_USUARIO.COMPANIA = s$compania$s
AND SP_USUARIO.CICLO  BETWEEN s$cicloInicial$s AND s$cicloFinal$s
AND SP_USUARIO.LECTURA  = SP_USUARIO.LECTURA1
AND SP_USUARIO.CONSUMO  = SP_USUARIO.CONSUMOPROM
AND SP_USUARIO.ESTADO NOT IN ('R')
AND SP_USUARIO.BANCOPERPROCESO IS NULL
ORDER BY  SP_USUARIO.CICLO,
          SP_USUARIO.CODIGORUTA
