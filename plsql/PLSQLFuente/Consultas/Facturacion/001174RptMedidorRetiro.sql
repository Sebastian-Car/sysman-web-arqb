SELECT SP_USUARIO.COMPANIA,
  SP_USUARIO.CICLO,
  SP_USUARIO.CODIGORUTA,
  (Trim(primerapellido)
  || ' '
  || Trim(segundoapellido)) AS NOMBRECOMPLETO, 
  SP_USUARIO.ANO,
  SP_USUARIO.PERIODO,
  SP_USUARIO.MATRICULA,
  SP_USUARIO.DIRTECNICA,
  SP_ESTRATOS.CODIGO AS ESTRATO,
  SP_ESTRATOS.NOMBRE AS ESTRATO_NOMBRE,
  SP_USOS.CODIGO     AS USO,
  SP_USOS.NOMBRE     AS USO_NOMBRE,
  SP_USUARIO.LECTURA,
  SP_USUARIO.TELEFONO,
  SP_USUARIO.MEDIDOR
FROM SP_USUARIO LEFT
JOIN SP_ESTRATOS
ON SP_USUARIO.ESTRATO   = SP_ESTRATOS.CODIGO
AND SP_USUARIO.USO      = SP_ESTRATOS.USO
AND SP_USUARIO.COMPANIA = SP_ESTRATOS.COMPANIA 
LEFT JOIN SP_USOS
ON SP_ESTRATOS.USO       = SP_USOS.CODIGO 
AND SP_ESTRATOS.COMPANIA = SP_USOS.COMPANIA
WHERE SP_USUARIO.COMPANIA = 's$compania$s' 
AND SP_USUARIO.CODIGORUTA BETWEEN 's$codigoInicialSeleccion$s' 
AND 's$codigoFinalSeleccion$s' 
 s$ciclo$s
