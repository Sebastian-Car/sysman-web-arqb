SELECT SP_USUARIO.COMPANIA, 
    SP_USUARIO.CICLO, 
    SP_USUARIO.PERIODO, 
    SP_USUARIO.CODIGORUTA, 
    SP_FACTURADO.CONCEPTO, 
    SP_FACTURADO.VALOR_FACTURADO, 
    SP_USUARIO.ANO, 
    SP_USUARIO.ESTADO, 
    SP_USUARIO.PERIODO, 
    SP_USUARIO.USO, 
    SP_USUARIO.PRIMERAPELLIDO, 
    SP_USUARIO.SEGUNDOAPELLIDO, 
    SP_USUARIO.NOMBRES, 
    SP_USUARIO.CODIGOINTERNO, 
    SP_USUARIO.DIRTECNICA, 
    SP_USUARIO.DIRGUIA, 
    SP_USUARIO.DIRCORRESPONDENCIA, 
    SP_USUARIO.MEDIDOR, 
    SP_USUARIO.LECTURA1, 
    SP_USUARIO.LECTURA, 
    SP_USUARIO.LECTURAAFORO, 
    (LECTURA-LECTURA1) AS CNSAF,
    SP_USUARIO.CONSUMOPROM AS CAFORO,
    SP_USUARIO.HOGAR_COMUNITARIO,
    SP_USUARIO.INQUILINATOS,
    SP_FACTURADO.DEUDA,
    CASE WHEN FECHAPAGOPERANTERIOR IS NOT NULL THEN SP_FACTURADO.VALOR_FACTURADO ELSE 0 END AS VALORPAGO
FROM SP_USUARIO
LEFT JOIN SP_FACTURADO 
  ON  SP_USUARIO.COMPANIA = SP_FACTURADO.COMPANIA
  AND SP_USUARIO.CICLO = SP_FACTURADO.CICLO 
  AND SP_USUARIO.CODIGORUTA = SP_FACTURADO.CODIGORUTA
WHERE SP_USUARIO.COMPANIA = s$compania$s  
  AND SP_USUARIO.CICLO = s$ciclo$s 
  AND SP_FACTURADO.CONCEPTO = 44 
  AND SP_USUARIO.ESTADO IN ('A')
ORDER BY SP_USUARIO.COMPANIA, SP_USUARIO.CICLO, SP_USUARIO.CODIGORUTA
