SELECT U.COMPANIA, 
	U.CICLO, 
	U.CODIGORUTA, 
	U.CHAPETAS, 
	U.PRIMERAPELLIDO, 
	U.SEGUNDOAPELLIDO, 
	U.NOMBRES, 
	DIRTECNICA,
	TO_CHAR(Q.FECHA_MODIF,'DD/MM/YYYY') AS FECHARENTRADANUEVOUSUARIO 
FROM SP_USUARIO U 
	LEFT JOIN (
		SELECT QU.COMPANIA, 
			QU.CICLO, 
			QU.CODIGORUTA, 
			S.CHAPETAS, 
			TO_CHAR(S.DATE_MODIFIED,'DD/MM/YYYY') AS FECHA_MODIF
		FROM (
			SELECT COMPANIA,
				CICLO,
				CODIGORUTA, 
				Max(CONSECUTIVO) AS CONSECUTIVO, 
				TO_CHAR(Max(TO_CHAR(DATE_MODIFIED,'DD/MM/YYYY HH:MI:SS')),'DD/MM/YYYY') AS FECHA
			FROM SP_AUDITORIA_USUARIO
			WHERE CHAPETAS NOT IN(CHAPETAS_ANT)
			GROUP BY COMPANIA, 
				CICLO, 
				CODIGORUTA) QU INNER JOIN SP_AUDITORIA_USUARIO S
				ON QU.COMPANIA 	  = S.COMPANIA
 				AND QU.CICLO       = S.CICLO
 				AND QU.CODIGORUTA  = S.CODIGORUTA 
 				AND QU.CONSECUTIVO = S.CONSECUTIVO
				WHERE S.CHAPETAS NOT IN (0)) Q
	ON U.COMPANIA 	 = Q.COMPANIA
	AND U.CICLO 	 = Q.CICLO
	AND U.CODIGORUTA = Q.CODIGORUTA 
WHERE U.COMPANIA 	 = s$compania$s 
	AND U.CICLO 	 = s$ciclo$s
	AND U.CHAPETAS 	 NOT IN (0)
