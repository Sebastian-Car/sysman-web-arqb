SELECT  SP_USUARIO.CODIGORUTA,
        SP_USUARIO.CODIGOINTERNO,
        SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO || ' ' || SP_USUARIO.NOMBRES NOMBREUSUARIO,
        SP_USUARIO.CARGOFIJO,
        SP_USUARIO.VALCONSUMO + SP_USUARIO.VALCONSUMOPROM TOTALVALORCONSUMO,
        SP_USUARIO.TOTALALCANTARILLADO,
        SP_USUARIO.TOTALASEO,
        SP_USUARIO.TOTFACTURAPERACTUAL - SP_USUARIO.TOTALDEUDA - SP_USUARIO.TOTALASEO - SP_USUARIO.TOTALALCANTARILLADO - SP_USUARIO.TOTALACUEDUCTO OTROSCARGOS,
        SP_USUARIO.TOTALDEUDA,
        SP_USUARIO.TOTALACUEDUCTO,
        SP_USUARIO.TOTALALUMBRADO,
        SP_USUARIO.ABONOS,
        NVL(SP_USUARIO.TOTALDEUDA,0) + NVL(SP_USUARIO.TOTALACUEDUCTO,0) + NVL(SP_USUARIO.TOTALALUMBRADO,0) TOTAL,
        SP_USUARIO.TOTFACTURAPERACTUAL,
		s$pago$s
        SP_USUARIO.FECHAPAGOPERPROCESO
FROM SP_USUARIO
  s$relacion$s
WHERE SP_USUARIO.COMPANIA = s$compania$s
  s$condicionCiclo$s
  AND SP_USUARIO.ESTADO NOT IN ('R','S')
  AND SP_USUARIO.BANCOPERPROCESO IS NOT NULL
  AND SP_USUARIO.FECHAPAGOPERPROCESO BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND SP_USUARIO.BANCOPERPROCESO BETWEEN s$bancoInicial$s AND s$bancoFinal$s
s$pagos$s
