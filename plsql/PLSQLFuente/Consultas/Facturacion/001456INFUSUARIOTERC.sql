SELECT USUARIO.EMPRESAASEOEXT,
  USUARIO.CICLO,
  USUARIO.CODIGORUTA,
  USUARIO.CODIGOINTERNO,
  USUARIO.NOMBRES || ' '|| USUARIO.PRIMERAPELLIDO || ' '|| USUARIO.SEGUNDOAPELLIDO AS NOMBRE,
  USUARIO.FECHAINGASEOEXT,
  USUARIO.CODIGO_EXTERNO,
  EMPRESAS_TERCERIZA.NIT,
  EMPRESAS_TERCERIZA.NOMBRE NOMEMPRESA
  FROM SP_USUARIO USUARIO
   INNER JOIN SP_EMPRESAS_TERCERIZA EMPRESAS_TERCERIZA
       ON USUARIO.COMPANIA       = EMPRESAS_TERCERIZA.COMPANIA
      AND USUARIO.EMPRESAASEOEXT = EMPRESAS_TERCERIZA.ID
WHERE USUARIO.COMPANIA        = s$compania$s
  AND USUARIO.EMPRESAASEOEXT IS NOT NULL
  AND USUARIO.EMPRESAASEOEXT = CASE WHEN 's$empresa$s' LIKE 'T'
                                    THEN USUARIO.EMPRESAASEOEXT
                                    ELSE 's$empresa$s'
                               END 
  s$condicionCiclo$s
