SELECT U.USO,
  	US.NOMBRE NOMBREUSO,
	U.ESTRATO,
	E.NOMBRE NOMBRESTRATO,
	SUM(CASE WHEN PERIODOSATRASO <= 1 THEN TOTALDEUDA ELSE 0 END) E1,
	SUM(CASE WHEN PERIODOSATRASO = 2 THEN TOTALDEUDA ELSE 0 END) E2,
	SUM(CASE WHEN PERIODOSATRASO = 3 THEN TOTALDEUDA ELSE 0 END) E3,
	SUM(CASE WHEN PERIODOSATRASO = 4 THEN TOTALDEUDA ELSE 0 END) E4,
	SUM(CASE WHEN PERIODOSATRASO = 5 THEN TOTALDEUDA ELSE 0 END) E5,
	SUM(CASE WHEN PERIODOSATRASO = 6 THEN TOTALDEUDA ELSE 0 END) E6,
	SUM(CASE WHEN PERIODOSATRASO > 6 THEN TOTALDEUDA ELSE 0 END) E7,
	SUM(U.TOTALDEUDA) AS TOTALE
FROM SP_USUARIO U
	INNER JOIN SP_USOS US
		ON U.COMPANIA = US.COMPANIA
		AND U.USO 	  = US.CODIGO
  	INNER JOIN SP_ESTRATOS E
    	ON U.COMPANIA = E.COMPANIA
    	AND U.USO 	  = E.USO
    	AND U.ESTRATO = E.CODIGO
WHERE U.COMPANIA = s$compania$s
	AND U.CICLO  = s$cicloInicial$s
	AND U.BANCOPERPROCESO IS NULL
GROUP BY U.USO,
  	US.NOMBRE,
	U.ESTRATO,
  	E.NOMBRE
