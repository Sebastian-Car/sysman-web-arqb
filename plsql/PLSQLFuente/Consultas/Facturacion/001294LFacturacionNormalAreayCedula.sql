SELECT
  NVL(RECARGOACUEDUCTO,0)+NVL(RECARGOASEO,0) TOTALRECARGO,
  TOTFACTURAPERACTUAL-TOTALDEUDA-TOTALASEO-TOTALALCANTARILLADO-TOTALACUEDUCTO OTROSCARGOS,
  VALCONSUMO + VALCONSUMOPROM+ ACSINMEDICION TOTALVALORCONSUMO,
  USUARIO.ESTADO,
  USUARIO.CODIGORUTA,
  NVL(USUARIO.PRIMERAPELLIDO,' ') ||' '|| NVL(USUARIO.SEGUNDOAPELLIDO,' ')||' '|| NVL(USUARIO.NOMBRES,' ') NOMBRECOMPLETO,
  USUARIO.LECTURA,
  USUARIO.PERIODOSATRASO,
  USUARIO.BANCOPERPROCESO,
  USUARIO.NOFECHAPAGOPERPROCESO,
  USUARIO.FECHAPAGOPERPROCESO,
  USUARIO.LECTURAAFORO,
  USUARIO.PAQUETEPAGOPERPROCESO,
  USUARIO.AREA,
  USUARIO.NIT,
  USUARIO.CODIGOINTERNO,
  USUARIO.DIRTECNICA,
  USUARIO.USO,
  USUARIO.ESTRATO,
  USUARIO.LECTURA1,
  USUARIO.TOTFACTURAPERACTUAL,
  USUARIO.TOTALDEUDA,
  USUARIO.CONSUMOPROM,
  USUARIO.CONSUMO,
  USUARIO.CARGOFIJO,
  USUARIO.TOTALACUEDUCTO,
  USUARIO.TOTALALCANTARILLADO,
  USUARIO.TOTALASEO
FROM SP_USUARIO USUARIO
WHERE USUARIO.ESTADO NOT IN ('R','S')
AND USUARIO.COMPANIA = s$compania$s
s$condicion$s
AND USUARIO.CODIGORUTA BETWEEN s$codigoInicial$s AND s$codigoFinal$s
AND USO  || ESTRATO BETWEEN s$usoIni$s || s$estratoIni$s AND s$usoFin$s || s$estratoFin$s
