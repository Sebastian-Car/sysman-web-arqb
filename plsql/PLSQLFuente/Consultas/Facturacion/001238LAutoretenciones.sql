SELECT CODIGORUTA, 
	NVL(PRIMERAPELLIDO,' ')||' '||NVL(SEGUNDOAPELLIDO,' ')||' '||NVL(NOMBRES,' ') NOMBRECOMPLETO,
	CODIGOINTERNO,
	TOTFACTURAPERACTUAL TOTAL_FACTURA,
	ROUND(TOTFACTURAPERACTUAL*s$retencion$s/100) RETENCION,
	TOTFACTURAPERACTUAL+ROUND(TOTFACTURAPERACTUAL*s$retencion$s/100) AS TOTALRET,
	USO,
	ESTRATO 
FROM SP_USUARIO U
WHERE COMPANIA=s$compania$s 
	s$condicionCiclo$s
	AND ESTADO NOT IN ('R','S')
	s$condicionUso$s
	AND BANCOPERPROCESO IS NOT NULL
ORDER BY CICLO
