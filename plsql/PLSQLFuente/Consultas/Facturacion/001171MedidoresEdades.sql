SELECT  CASE WHEN SP_USUARIO.COMPANIA IS NULL THEN 'SIN ASIGNAR'ELSE 'ASIGNADO' END CLASE,
        SUM(CASE WHEN ABS(TO_DATE(TO_CHAR(FECHAINST,'DD/MM/YYYY'))-SYSDATE)/365<=1 THEN 1 ELSE 0 END )CEROYUNO,
        SUM(CASE WHEN  1 < ABS(TO_DATE(TO_CHAR(FECHAINST,'DD/MM/YYYY'))-SYSDATE)/365 AND ABS(TO_DATE(TO_CHAR(FECHAINST,'DD/MM/YYYY'))-SYSDATE)/365 <=3 THEN 1 ELSE 0 END)  UNOYTRES,
        SUM(CASE WHEN 3 <  ABS(TO_DATE(TO_CHAR(FECHAINST,'DD/MM/YYYY'))-SYSDATE)/365 THEN 1 ELSE 0 END) MASTRES,
        SUM(CASE WHEN FECHAINST IS NULL THEN 1 ELSE 0 END ) SINFECHA,
        SUM(CASE WHEN SP_USUARIO.COMPANIA IS NULL THEN 1 ELSE 0 END ) SINASIGNAR 
FROM SP_MEDIDOR 
  LEFT JOIN SP_USUARIO 
    ON SP_MEDIDOR.COMPANIA = SP_USUARIO.COMPANIA  
    AND SP_MEDIDOR.CONSECUTIVO  =  SP_USUARIO.MEDIDOR
    AND SP_MEDIDOR.CICLO = SP_USUARIO.CICLO
WHERE SP_MEDIDOR.COMPANIA = s$compania$s
 s$condicion$s
 GROUP BY CASE WHEN SP_USUARIO.COMPANIA IS NULL THEN 'SIN ASIGNAR' ELSE'ASIGNADO' END
