SELECT ABONOS.BANCO,
  ABONOS.CODIGORUTA,
   ABONOS.ANO ||  ABONOS.PERIODO  AS NOMBREDEPERIODO,
   PRIMERAPELLIDO || ' '||  SEGUNDOAPELLIDO || ' '||  NOMBRES AS NOMBRECOMPLETO,
  ABONOS.VALOR   AS VLRTOTAL,
  PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO (ABONOS.COMPANIA,ABONOS.ANO,ABONOS.PERIODO,NULL) AS TXTNOMPERIODO,
 TO_CHAR( ABONOS.FECHA,'DD/MM/YYYY')   AS FECHAABONO,
  BANCOS.NOMBRE   AS NOMBREBANCO
FROM (SP_ABONOS ABONOS
LEFT JOIN SP_USUARIO USUARIO
ON (ABONOS.CODIGORUTA = USUARIO.CODIGORUTA)
AND (ABONOS.CICLO     = USUARIO.CICLO)
AND (ABONOS.COMPANIA  = USUARIO.COMPANIA))
LEFT JOIN SP_BANCOS BANCOS
ON (ABONOS.COMPANIA = BANCOS.COMPANIA)
AND (ABONOS.BANCO   = BANCOS.CODIGO)
WHERE  ABONOS.COMPANIA=s$compania$s
AND TO_DATE(TO_CHAR(ABONOS.FECHA,'DD/MM/YYYY'),'DD/MM/YYYY') BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE( 's$fechaFinal$s'
,'DD/MM/YYYY')
s$ciclo$s
s$codigo$s
