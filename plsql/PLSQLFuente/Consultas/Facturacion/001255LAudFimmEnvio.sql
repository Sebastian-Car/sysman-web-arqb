SELECT  SP_PLANOSDIGITALENVIO.CONSECUTIVO, 
        SP_PLANOSDIGITALENVIO.CICLO, 
        SP_PLANOSDIGITALENVIO.CODIGOINICIAL,
        SP_PLANOSDIGITALENVIO.CODIGOFINAL,

        SP_PLANOSDIGITALENVIO.NUMEROUSUARIOS,

        SP_PLANOSDIGITALENVIO.USUARIO,

        SP_PLANOSDIGITALENVIO.FECHA
 AS FECHA

FROM  SP_PLANOSDIGITALENVIO 
WHERE SP_PLANOSDIGITALENVIO.COMPANIA = s$compania$s

  AND SP_PLANOSDIGITALENVIO.FECHA BETWEEN TO_DATE('s$fechaInicial$s', 'DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s', 'DD/MM/YYYY')
