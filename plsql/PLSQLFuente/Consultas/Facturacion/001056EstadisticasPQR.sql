SELECT  AFAVOR,
        CLASEPROBLEMA,
        DIRECCION,
        ENCONTRA,
        FECHASOLICITUD,
        FECHASOLUCION,
        INDFAVOREMPRESA,
        NIT,
        NOMBRE,
        NOMBRECOMPANIA,
        NUIR,
        NUMERO, 
        PENDIENTE,
        PROBLEMA,
        PROBLEMACODIGO,
        SOLUCION,
        TELEFONO, 
        TIPOREQUERIMIENTO1,
        COUNT(*) OVER (PARTITION BY TIPOREQUERIMIENTO1,CLASEPROBLEMA) TOTALCASOS,
        SUM(AFAVOR) OVER (PARTITION BY TIPOREQUERIMIENTO1,CLASEPROBLEMA) TOTALAFAVOR,
        SUM(ENCONTRA) OVER (PARTITION BY TIPOREQUERIMIENTO1,CLASEPROBLEMA) TOTALENCONTRA,
        SUM(PENDIENTE) OVER (PARTITION BY TIPOREQUERIMIENTO1,CLASEPROBLEMA) TOTALPENDIENTE 
FROM
(
 SELECT DISTINCT COMPANIA.NOMBRE NOMBRECOMPANIA,
  COMPANIA.DIRECCION,
  COMPANIA.TELEFONO,
  COMPANIA.NITCOMPANIA NIT,
  COMPANIA.NITCOMPANIA NUIR,
  D_ORDENTRABAJO.NUMERO,
  (SELECT NOMBRE
   FROM SP_CLASEPROBLEMA
   WHERE COMPANIA = COMPANIA.CODIGO
    AND  SP_CLASEPROBLEMA.CLASEPROBLEMA = D_ORDENTRABAJO.CLASEPROBLEMA) CLASEPROBLEMA,
  TO_CHAR(D_ORDENTRABAJO.FECHASOLUCION,'DD/MM/YYYY') FECHASOLUCION,
  D_ORDENTRABAJO.SOLUCION,
  D_ORDENTRABAJO.PROBLEMA,
  PROBLEMA.NOMBRE,
  CASE
    WHEN D_ORDENTRABAJO.INDFAVOREMPRESA NOT IN (0)
    AND D_ORDENTRABAJO.FECHASOLUCION    IS NOT NULL
    THEN 1
    ELSE 0
  END AFAVOR,
  CASE
    WHEN D_ORDENTRABAJO.INDFAVOREMPRESA IN (0)
    AND D_ORDENTRABAJO.FECHASOLUCION    IS NOT NULL
    THEN 1
    ELSE 0
  END ENCONTRA,
  D_ORDENTRABAJO.INDFAVOREMPRESA,
  CASE
    WHEN D_ORDENTRABAJO.FECHASOLUCION IS NULL
    THEN 1
    ELSE 0
  END PENDIENTE,
  TO_CHAR(ORDENTRABAJO.FECHASOLICITUD,'DD/MM/YYYY') FECHASOLICITUD,
  PROBLEMA.CODIGO PROBLEMACODIGO,
  DECODE(
  CASE
    WHEN TIPOREQUERIMIENTO IS NOT NULL
    THEN TIPOREQUERIMIENTO
    ELSE 'NA'
  END,
  'C','Consulta',
  'Q','Queja',
  'RE','Reclamo',
  'R','Recurso de Reposición',
  'P','Petición',
  'RA','Recurso de Reposición  y  Subsidiario de Apelación',
  'NA','No Tiene asignado Tipo de Requerimiento') TIPOREQUERIMIENTO1 
FROM COMPANIA  
INNER JOIN SP_ORDENTRABAJO ORDENTRABAJO
ON COMPANIA.CODIGO = ORDENTRABAJO.COMPANIA
INNER JOIN SP_D_ORDENTRABAJO D_ORDENTRABAJO
ON ORDENTRABAJO.COMPANIA  = D_ORDENTRABAJO.COMPANIA
AND ORDENTRABAJO.NUMORDEN = D_ORDENTRABAJO.ORDENTRABAJO
AND ORDENTRABAJO.CLASEDOC = D_ORDENTRABAJO.CLASEDOC
INNER JOIN SP_PROBLEMA PROBLEMA
ON D_ORDENTRABAJO.COMPANIA         = PROBLEMA.COMPANIA
AND D_ORDENTRABAJO.PROBLEMA        = PROBLEMA.CODIGO
AND D_ORDENTRABAJO.CLASEPROBLEMA   = PROBLEMA.CLASEPROBLEMA 
WHERE D_ORDENTRABAJO.CLASEPROBLEMA =
  CASE
    WHEN 's$tipoServicio$s'='T'
    THEN D_ORDENTRABAJO.CLASEPROBLEMA
    ELSE 's$tipoServicio$s'
  END
AND
  CASE
    WHEN 's$claseDoc$s'='T'
    THEN 'T'
    ELSE ORDENTRABAJO.CLASEDOC
  END = 's$claseDoc$s'
AND ORDENTRABAJO.FECHASOLICITUD BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
AND ORDENTRABAJO.COMPANIA = s$compania$s
AND PROBLEMA.CODIGO      IS NOT NULL
ORDER BY ORDENTRABAJO.COMPANIA)
