SELECT 
	CICLO.NUMERO, 
	CONJUNTA_CONVENIO.TSUSCCONVE, 
	CONJUNTA_CONVENIO.TOTALCONVENIO, 
	CONJUNTA_ASEO.TSUSCASEO, 
	CONJUNTA_ASEO.TOTALASEO
FROM SP_CICLO CICLO 
	LEFT JOIN ( SELECT 
				      HISTORIA_EXTERNA.COMPANIA,
				      HISTORIA_EXTERNA.CICLO, 
				      COUNT(HISTORIA_EXTERNA.CODIGORUTA) AS TSUSCASEO,
				      SUM(HISTORIA_EXTERNA.VALORASEO) AS TOTALASEO 
				 FROM SP_HISTORIA_EXTERNA HISTORIA_EXTERNA 
				WHERE COMPANIA 				 =  s$compania$s 
				  AND s$condicionCiclo$s ANO =  s$anio$s 
				  AND PERIODO 				 = 's$periodo$s'
				GROUP BY HISTORIA_EXTERNA.COMPANIA,
					  HISTORIA_EXTERNA.CICLO
			  ) CONJUNTA_ASEO 
		   ON CICLO.COMPANIA    = CONJUNTA_ASEO.COMPANIA 
		  AND CICLO.NUMERO   	= CONJUNTA_ASEO.CICLO 		   
	LEFT JOIN ( SELECT 
					HISTORIA_CONVENIOS.COMPANIA,
					HISTORIA_CONVENIOS.CICLO,
					COUNT(HISTORIA_CONVENIOS.CODIGORUTA) AS TSUSCCONVE,  
					SUM(HISTORIA_CONVENIOS.TOTAL) AS TOTALCONVENIO 
				FROM  SP_HISTORIA_CONVENIOS HISTORIA_CONVENIOS 
				WHERE COMPANIA  				= s$compania$s 
				  AND s$condicionCiclo$s ANO 	= s$anio$s
				  AND PERIODO 					= 's$periodo$s' 
				  AND NOCOBRAR 					= 0 
				GROUP BY HISTORIA_CONVENIOS.COMPANIA,
					HISTORIA_CONVENIOS.CICLO
			 ) CONJUNTA_CONVENIO 
		   ON CICLO.COMPANIA  = CONJUNTA_CONVENIO.COMPANIA
		  AND CICLO.NUMERO    = CONJUNTA_CONVENIO.CICLO
WHERE CONJUNTA_ASEO.TOTALASEO IS NOT NULL
   OR CONJUNTA_CONVENIO.TOTALCONVENIO IS NOT NULL
