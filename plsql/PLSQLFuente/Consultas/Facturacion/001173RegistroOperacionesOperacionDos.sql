SELECT RS.CODIGORUTA, 
	CASE ITO.TIPO_OPERACION 
    	WHEN '001' THEN 'SUSPENSION' 
    	WHEN '002' THEN 'CORTE'
    	WHEN '003' THEN 'RECONEXION' 
    	WHEN '004' THEN 'REINSTALACION'
  	END TIPO_OPERACION,
	RS.DIRTECNICA,
	RS.NOMBREC NOMBRES,
	M.CODIGO MEDIDOR,
	RS.LECTURA,
	RS.PERIODOSATRASO PERATRASO,
	RS.USO,
	RS.ESTRATO,
	ROWNUM CONSECUTIVO,
	ITO.OBSERVACIONES,
	ITO.DESCRIPCION
FROM SP_INFO_TIPO_OPERACION ITO
  INNER JOIN SP_MEDIDOR M
     ON ITO.COMPANIA   = M.COMPANIA
    AND ITO.CICLO      = M.CICLO
    AND ITO.CODIGORUTA = M.CODIGORUTA
  INNER JOIN  
	(SELECT CICLO,
		CODIGORUTA,
		ANO,
		PERIODO,
		NVL(PRIMERAPELLIDO,' ')||' '||NVL(SEGUNDOAPELLIDO,' ')||' '||NVL(NOMBRES,' ') NOMBREC,
		MEDIDOR,
		PERIODOSATRASO,
		USO,
		ESTRATO,
		LECTURA,
		DIRTECNICA,
		COMPANIA
	FROM SP_USUARIO U
	WHERE COMPANIA=s$compania$s 
		AND FECHAPAGOPERPROCESO IS NOT NULL 
		s$condicionFechaPagoPerProceso$s
		AND ESTADO IN ('C','S') 
		s$condicionCiclo$s
	UNION
	SELECT U.CICLO, 
		U.CODIGORUTA, 
		U.ANO,
		U.PERIODO,
		NVL(PRIMERAPELLIDO,' ')||' '||NVL(SEGUNDOAPELLIDO,' ')||' '||NVL(NOMBRES,' ') NOMBREC,
		U.MEDIDOR, 
		U.PERIODOSATRASO, 
		U.USO, 
		U.ESTRATO, 
		U.LECTURA, 
		U.DIRTECNICA,
		U.COMPANIA
	FROM SP_USUARIO U 
		INNER JOIN SP_ABONOS A
			ON U.COMPANIA 	 = A.COMPANIA
			AND U.CICLO 	 = A.CICLO
			AND U.ANO 		 = A.ANO
			AND U.PERIODO 	 = A.PERIODO
			AND U.CODIGORUTA = A.CODIGORUTA
	WHERE U.COMPANIA=s$compania$s
		AND U.ESTADO IN ('C','S') 
		s$condicionFecha$s
		s$condicionCiclo$s
	) RS
    ON ITO.COMPANIA   =RS.COMPANIA
	AND ITO.CICLO 	  =RS.CICLO
	AND ITO.ANO 	  =RS.ANO
	AND ITO.PERIODO   =RS.PERIODO
	AND ITO.CODIGORUTA=RS.CODIGORUTA
WHERE ITO.TIPO_OPERACION IN ('003','004')
ORDER BY ITO.CICLO, 
	ITO.CODIGORUTA, 
	ITO.CONSECUTIVO
