SELECT 
CASE WHEN CONSUMO-(CONSUMOPROM*(1+(s$limiteSuperior$s/100)))>=0 
     THEN 'Superior al promedio'
     ELSE
        CASE WHEN CONSUMO-(CONSUMOPROM*(1-(s$limiteInferior$s/100)))<=0
             THEN 'Inferior al promedio'
             ELSE
                CASE WHEN (Consumo>=500 AND USO ='01' )OR (CONSUMO>=9000 AND USO <> '01')
                     THEN 'Elevado'
                     ELSE ''
                     END
             END
      END AS OBSERVACIONES,
CONSUMO-(CONSUMOPROM*(1+(s$limiteSuperior$s/100))) AS DIFSUPERIORAPROMEDIO,
CONSUMO-(CONSUMOPROM*(1-(s$limiteInferior$s/100))) AS DIFINFERIORAPROIMEDIO,
1 AS CNSAF,

(SP_USUARIO.PRIMERAPELLIDO||' '||SP_USUARIO.SEGUNDOAPELLIDO||' '||SP_USUARIO.NOMBRES) NOMBREUSUARIO,
SP_USUARIO.DIRTECNICA, 
SP_USUARIO.USO, 
SP_USUARIO.LECTURA1,
SP_USUARIO.LECTURAAFORO, 
SP_USUARIO.CONSUMO,
SP_USUARIO.CONSUMOPROM, 
SP_USUARIO.CODIGOINTERNO, 
SP_USUARIO.ACUMULADO,
SP_USUARIO.ANO, 
SP_USUARIO.PERIODO,
SP_USUARIO.LECTURA,
SP_USUARIO.CODIGORUTA, 
SP_PROBLEMA.CODIGO,
SP_PROBLEMA.NOMBRE, 
SP_USUARIO_PROBLEMA.PERIODO AS period_problem, 
SP_USUARIO.PERIODO
FROM SP_USUARIO
INNER JOIN SP_USUARIO_PROBLEMA 

ON SP_USUARIO.COMPANIA = SP_USUARIO_PROBLEMA.COMPANIA
AND SP_USUARIO.CICLO = SP_USUARIO_PROBLEMA.CICLO
AND SP_USUARIO.CODIGORUTA = SP_USUARIO_PROBLEMA.CODIGORUTA 
AND SP_USUARIO.PERIODO = SP_USUARIO_PROBLEMA.PERIODO

INNER JOIN SP_PROBLEMA 
ON SP_USUARIO_PROBLEMA.COMPANIA = SP_PROBLEMA.COMPANIA 
AND SP_USUARIO_PROBLEMA.PROBLEMA = SP_PROBLEMA.CODIGO 
AND SP_USUARIO_PROBLEMA.CLASE = SP_PROBLEMA.CLASEPROBLEMA
AND SP_USUARIO_PROBLEMA.PERIODO=SP_USUARIO.periodo
AND SP_USUARIO_PROBLEMA.ANO=SP_USUARIO.ANO

WHERE (SP_USUARIO.CONSUMO)>0 
And((SP_USUARIO.CONSUMO)>=(ConsumoProm*(1+(s$limiteSuperior$s/100)))
Or (SP_USUARIO.CONSUMO)<=(ConsumoProm*(1-(s$limiteInferior$s/100)))) 

AND ((SP_USUARIO.COMPANIA)= s$compania$s
AND ((SP_USUARIO.CICLO)=s$ciclo$s)) OR (((SP_USUARIO.USO)='01')
AND ((SP_USUARIO.CONSUMO)>=500) AND ((SP_USUARIO.COMPANIA)= s$compania$s) 
AND ((SP_USUARIO.CICLO)=s$ciclo$s)) 
OR (((SP_USUARIO.USO)='01') AND ((SP_USUARIO.CONSUMO)>=9000) 
AND ((SP_USUARIO.COMPANIA)=s$compania$s) 
AND ((SP_USUARIO.CICLO)=s$ciclo$s))
ORDER BY SP_USUARIO.COMPANIA, SP_USUARIO.CICLO, SP_USUARIO.CODIGORUTA
