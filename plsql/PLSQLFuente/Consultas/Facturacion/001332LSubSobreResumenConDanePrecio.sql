SELECT SP_USUARIO.COMPANIA, 
   SP_ESTRATOS.NOMBRE AS NOMBREESTRATO, 
  SP_USOS.NOMBRE  AS NOMBREUSO, 
  SP_USUARIO.CICLO, 
  SP_USUARIO.ANO, 
  SP_USUARIO.PERIODO, 
  SP_USUARIO.ESTADO, 
  SP_USUARIO.SUBSIDIO, 
  SP_USUARIO.SOBREPRECIO, 
  SP_USUARIO.SUBACUEDUCTO, 
  SP_USUARIO.SUBALCANTARILLADO, 
  SP_USUARIO.SUBASEO, 
  SP_USUARIO.SUBFIJO, 
  SP_USUARIO.SUBFIJOALC, 
  SP_USUARIO.SUBCONSUMOAC, 
  SP_USUARIO.SUBCONSUMOAL, 
  SP_USUARIO.SOBREACUEDUCTO, 
  SP_USUARIO.SOBREALCANTARILLADO, 
  SP_USUARIO.SOBREASEO, 
  SP_USUARIO.SOBREFIJO, 
  SP_USUARIO.SOBREFIJOALC, 
  SP_USUARIO.SOBRECONSUMOAC, 
  SP_USUARIO.SOBRECONSUMOAL, 
  SP_USUARIO.CODIGORUTA, 
  SP_USUARIO.DIRTECNICA, 
  SP_USUARIO.CODIGOINTERNO, 
  SP_USUARIO.USO, 
  SP_USUARIO.ESTRATO, 
  SP_USUARIO.SUBSINMEDICION, 
  SP_USUARIO.SUBALCSINMEDICION, 
  SP_USUARIO.SOBRESINMEDICION, 
  SP_USUARIO.SOBREALCSINMEDICION, 
  PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(SP_USUARIO.COMPANIA,SP_USUARIO.ANO,SP_USUARIO.PERIODO,NULL) NOMPERIODO, 
  PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(SP_USUARIO.COMPANIA, PCK_SERVICIOS_PUBLICOS_COM2.FC_ANOSIGUIENTE(SP_USUARIO.COMPANIA,SP_USUARIO.ANO, SP_USUARIO.PERIODO , ''), PCK_SERVICIOS_PUBLICOS_COM2.FC_PERIODOSIGUIENTE(SP_USUARIO.COMPANIA,SP_USUARIO.ANO, SP_USUARIO.PERIODO , ''), '') NOMBREANOPERIODOSIGUIENTE, 
  SP_USUARIO.CODIGODANE 
FROM SP_USUARIO 
  LEFT JOIN SP_ESTRATOS 
    ON  SP_USUARIO.COMPANIA = SP_ESTRATOS.COMPANIA 
    AND SP_USUARIO.ESTRATO = SP_ESTRATOS.CODIGO 
    AND SP_USUARIO.USO = SP_ESTRATOS.USO 
  LEFT JOIN SP_USOS 
    ON  SP_ESTRATOS.COMPANIA = SP_USOS.COMPANIA 
    AND SP_ESTRATOS.USO = SP_USOS.CODIGO 
WHERE SP_USUARIO.COMPANIA=s$compania$s 
  AND SP_USUARIO.CICLO BETWEEN 's$cicloIncial$s' AND 's$cicloFinal$s' 
  AND SP_USUARIO.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 
  AND SP_USUARIO.ESTADO   NOT IN('R') 
  AND (SP_USUARIO.SUBSIDIO NOT IN(0) 
  OR SP_USUARIO.SOBREPRECIO NOT IN(0)) 
