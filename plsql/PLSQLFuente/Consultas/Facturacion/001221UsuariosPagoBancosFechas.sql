SELECT SP_PAGO.VALORPAGO, 
  SP_PAGO.OPERACION, 
  SP_PAGO.USUARIO, 
  (SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO || ' ' || SP_USUARIO.NOMBRES) AS NOMBRECOMPLETO, 
  SP_PAGO.CODIGORUTA, 
  SP_PAGO.COMPANIA, 
  SP_USUARIO.ANO, 
  SP_USUARIO.PERIODO, 
  SP_PAGO.NUMEROPAQUETE, 
  SP_PAGO.CONSECUTIVO, 
  TO_CHAR(SP_PAGO.FECHA,'DD/MM/YYYY') AS STRFECHA, 
 TO_CHAR( SP_PAGO.FECHA,'DD/MM/YYYY')   AS FECHAPAGO, 
  SP_PAGO.BANCO, 
  SP_BANCOS.NOMBRE AS NOMBREBANCO, 
  SP_USUARIO.CODIGOINTERNO,
 LPAD(SP_PAGO.BANCO,2,'0')||LPAD(SP_PAGO.NUMEROPAQUETE,3,'0')||LPAD(SP_PAGO.CONSECUTIVO,3,'0')  CONSECUTIVOGENERAL 
FROM SP_PAGO 
LEFT JOIN SP_USUARIO 
  ON  SP_PAGO.COMPANIA    = SP_USUARIO.COMPANIA 
  AND SP_PAGO.CICLO      = SP_USUARIO.CICLO 
  AND SP_PAGO.CODIGORUTA = SP_USUARIO.CODIGORUTA 
LEFT JOIN SP_BANCOS 
  ON  SP_PAGO.COMPANIA = SP_BANCOS.COMPANIA 
  AND SP_PAGO.BANCO   = SP_BANCOS.CODIGO 
WHERE SP_PAGO.COMPANIA= 's$compania$s'
  AND SP_PAGO.USUARIO  
  BETWEEN NVL('s$usuarioInicial$s','') AND 's$usuarioFinal$s' 
  AND SP_PAGO.FECHA 
  BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY') 
  AND SP_PAGO.BANCO  
  BETWEEN 's$bancoInicial$s' AND 's$bancoFinal$s'
