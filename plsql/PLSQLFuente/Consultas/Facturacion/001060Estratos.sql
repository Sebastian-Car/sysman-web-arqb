SELECT SP_USOS.NOMBRE NOMBRE_USO,
  CLASIFICACIONSUIASEO, 
  CLASIFICACIONSUI,
  SP_ESTRATOS.NOMBRE NOMBRE_ESTRATO, 
  SP_ESTRATOS.CODIGO,
  SP_ESTRATOS.USO
FROM SP_ESTRATOS
LEFT JOIN SP_USOS
ON SP_ESTRATOS.COMPANIA  = SP_USOS.COMPANIA
AND SP_ESTRATOS.USO      = SP_USOS.CODIGO
WHERE SP_ESTRATOS.COMPANIA=s$compania$s
