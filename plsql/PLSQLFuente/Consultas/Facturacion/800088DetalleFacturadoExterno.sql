  SELECT   SP_USUARIO.CODIGO_EXTERNO, 
  SP_FACTURADO_EXTERNO.CONCEPTO, 
  SP_FACTURADO_EXTERNO.VALOR_RECAUDO,
  TO_CHAR(SP_FACTURADO_EXTERNO.FECHA_PAGO, 'DD/MM/YYYY') AS FECHA_PAGO, 
  SP_FACTURADO_EXTERNO.BANCO_PAGO
 FROM     SP_FACTURADO_EXTERNO 
 INNER JOIN SP_USUARIO 
  ON (SP_FACTURADO_EXTERNO.CODIGORUTA = SP_USUARIO.CodigoRuta)
  AND (SP_FACTURADO_EXTERNO.CICLO = SP_USUARIO.Ciclo) 
  AND (SP_FACTURADO_EXTERNO.COMPANIA = SP_USUARIO.Compania)
 WHERE    SP_FACTURADO_EXTERNO.FECHA_PAGO BETWEEN s$fechaInicial$s AND s$fechaFinal$s
 ORDER BY SP_FACTURADO_EXTERNO.FECHA_PAGO
