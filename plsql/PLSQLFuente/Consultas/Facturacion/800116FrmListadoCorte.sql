,SP_USUARIOSSINABONOS AS (
 SELECT SP_USUARIO.COMPANIA,
 SP_USUARIO.CICLO,
 SP_USUARIO.CODIGORUTA,
 SP_USUARIO.ANO,
 SP_USUARIO.PERIODO,
 SP_USUARIO.CODIGOINTERNO,
 SUM(SP_USUARIO.NOTACREDITO) AS SUMADENOTACREDITO
 FROM SP_USUARIO LEFT JOIN SP_ABONOS ON 
 SP_USUARIO.COMPANIA = SP_ABONOS.COMPANIA
 AND SP_USUARIO.CICLO = SP_ABONOS.CICLO
 AND SP_USUARIO.CODIGORUTA = SP_ABONOS.CODIGORUTA
 AND SP_USUARIO.ANO = SP_ABONOS.ANO
 AND SP_USUARIO.PERIODO = SP_ABONOS.PERIODO
 WHERE SP_ABONOS.COMPANIA IS NULL
 GROUP BY SP_USUARIO.COMPANIA,
 SP_USUARIO.CICLO,
 SP_USUARIO.CODIGORUTA,
 SP_USUARIO.ANO,
 SP_USUARIO.PERIODO,
 SP_USUARIO.CODIGOINTERNO)
