SELECT 
  PCK_SYSMAN_UTL.FC_STRZERO(ANO,4)|| PCK_SYSMAN_UTL.FC_STRZERO(PERIODO,2) AS PERIODOUNICO,
  RANGO.LIMITEINFERIOR|| ' - '|| RANGO.LIMITESUPERIOR AS DESCRANGO, 
  ESTADISTICAS_CONSUMO.COMPANIA,
  ESTADISTICAS_CONSUMO.RANGO,
  SUM(ESTADISTICAS_CONSUMO.CANTUSUARIOSALCANTARILLADO) AS CANTUSUARIOSALCANTARILLADO,
  SUM(ESTADISTICAS_CONSUMO.CANTUSUARIOSPROBLEMA)       AS CANTUSUARIOSPROBLEMA,
  SUM(ESTADISTICAS_CONSUMO.CARGOFIJOALCANTARILLADO)    AS CARGOFIJOALCANTARILLADO,
  SUM(ESTADISTICAS_CONSUMO.SUMAVALOR)                  AS SUMAVALOR,
  SUM(ESTADISTICAS_CONSUMO.SUMACONSUMO)                AS SUMACONSUMO,
  SUM(ESTADISTICAS_CONSUMO.SUMACONSUMOPROM)            AS SUMACONSUMOPROM,
  SUM(ESTADISTICAS_CONSUMO.FACTURADOPROMEDIO)          AS FACTURADOPROMEDIO,
  ESTADISTICAS_CONSUMO.CONSUMO,
  ESTADISTICAS_CONSUMO.CONSUMOPROMEDIO,
  ESTADISTICAS_CONSUMO.VCONSUMOALCANTARILLADO
FROM ((SP_ESTADISTICAS_CONSUMO ESTADISTICAS_CONSUMO
LEFT JOIN SP_ESTRATOS ESTRATOS
ON (ESTADISTICAS_CONSUMO.ESTRATO   = ESTRATOS.CODIGO)
AND (ESTADISTICAS_CONSUMO.USO      = ESTRATOS.USO)
AND (ESTADISTICAS_CONSUMO.COMPANIA = ESTRATOS.COMPANIA))
LEFT JOIN SP_USOS USOS
ON (ESTADISTICAS_CONSUMO.COMPANIA = USOS.COMPANIA)
AND (ESTADISTICAS_CONSUMO.USO     = USOS.CODIGO))
LEFT JOIN SP_RANGO RANGO
ON (ESTADISTICAS_CONSUMO.RANGO     = RANGO.CODIGO)
AND (ESTADISTICAS_CONSUMO.COMPANIA = RANGO.COMPANIA)
WHERE
PCK_SYSMAN_UTL.FC_STRZERO(Ano,4)|| PCK_SYSMAN_UTL.FC_STRZERO(periodo,2)  BETWEEN  PCK_SYSMAN_UTL.FC_STRZERO(s$anoInicial$s,4)|| PCK_SYSMAN_UTL.FC_STRZERO(s$periodoInicial$s,2)
AND PCK_SYSMAN_UTL.FC_STRZERO(s$anoFinal$s,4)|| PCK_SYSMAN_UTL.FC_STRZERO(s$periodoFinal$s,2)
AND ESTADISTICAS_CONSUMO.COMPANIA=s$compania$s
AND ESTADISTICAS_CONSUMO.SERVICIO='02'
s$cicloCond$s
GROUP BY PCK_SYSMAN_UTL.FC_STRZERO(ANO,4)|| PCK_SYSMAN_UTL.FC_STRZERO(PERIODO,2),
  RANGO.LIMITEINFERIOR|| ' - '|| RANGO.LIMITESUPERIOR , 
  ESTADISTICAS_CONSUMO.COMPANIA,
  ESTADISTICAS_CONSUMO.RANGO,
  ESTADISTICAS_CONSUMO.CONSUMO,
  ESTADISTICAS_CONSUMO.CONSUMOPROMEDIO,
  ESTADISTICAS_CONSUMO.VCONSUMOALCANTARILLADO,
  ESTADISTICAS_CONSUMO.SERVICIO
