SELECT  SP_USUARIO.CODIGORUTA, 
        SP_USUARIO.DIRTECNICA, 
        SP_USUARIO.DIRGUIA, 
        SP_USUARIO.PERIODOSATRASO, 
        SP_USUARIO.LECTURA, 
        SP_USUARIO.LECTURA1, 
        SP_USUARIO.CONSUMO, 
        SP_USUARIO.CONSUMOPROM
FROM SP_USUARIO
WHERE SP_USUARIO.COMPANIA = s$compania$s
  AND SP_USUARIO.CICLO = s$ciclo$s
  s$facturacionSitio$s
  AND NVL(SP_USUARIO.INDDESHABITADO,0) IN(0)
  AND NVL(SP_USUARIO.INDMEDCAMBIADO,0) IN(0) 
  AND SP_USUARIO.ESTADO IN('A')
  AND (LECTURA < LECTURA1 OR LECTURA-LECTURA1 = 0)
  s$cond$s
