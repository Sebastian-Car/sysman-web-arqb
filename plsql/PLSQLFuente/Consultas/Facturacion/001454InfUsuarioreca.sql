SELECT SP_HISTORIA_EXTERNA.ID_EMPRESA,
  SP_HISTORIA_EXTERNA.BANCO_PAGO,
  TO_CHAR(SP_HISTORIA_EXTERNA.FECHA_PAGO,'DD/MM/YYYY') AS FECHA_PAGO,
  SP_HISTORIA_EXTERNA.CODIGO AS CODIGO_EXTERNO,
  SP_HISTORIA_EXTERNA.VALORASEO,
  SP_HISTORIA_EXTERNA.CODIGORUTA,
  SP_HISTORIA_EXTERNA.ANO,
  SP_HISTORIA_EXTERNA.PERIODO,
  SP_BANCOS.NOMBRE                                                                      AS NOMBRE_BANCO,
  SP_USUARIO.PRIMERAPELLIDO ||' '|| SP_USUARIO.SEGUNDOAPELLIDO ||' '|| SP_USUARIO.NOMBRES AS SUSCRIPTOR,
  SP_EMPRESAS_TERCERIZA.NOMBRE                                                          AS EMPRESA,
  SP_USUARIO.CODIGOINTERNO 
FROM SP_HISTORIA_EXTERNA
LEFT JOIN SP_USUARIO
ON SP_HISTORIA_EXTERNA.COMPANIA    = SP_USUARIO.COMPANIA
AND SP_HISTORIA_EXTERNA.CICLO      = SP_USUARIO.CICLO
AND SP_HISTORIA_EXTERNA.CODIGORUTA = SP_USUARIO.CODIGORUTA
LEFT JOIN SP_BANCOS
ON SP_HISTORIA_EXTERNA.BANCO_PAGO = SP_BANCOS.CODIGO
AND SP_HISTORIA_EXTERNA.COMPANIA  = SP_BANCOS.COMPANIA
INNER JOIN SP_EMPRESAS_TERCERIZA
ON SP_HISTORIA_EXTERNA.ID_EMPRESA = SP_EMPRESAS_TERCERIZA.ID
AND SP_HISTORIA_EXTERNA.COMPANIA  = SP_EMPRESAS_TERCERIZA.COMPANIA
WHERE SP_USUARIO.COMPANIA='s$compania$s'  
s$filtros$s 
ORDER BY SP_HISTORIA_EXTERNA.BANCO_PAGO, SP_HISTORIA_EXTERNA.FECHA_PAGO,SP_HISTORIA_EXTERNA.ID_EMPRESA
