SELECT SS.COMPANIA, 
	SS.CLASESOLICITUD, 
	NUMERO,
 	DIRTECNICA, 
 	NIT,
  	TO_CHAR(FECHASOLICITUD,'DD/MM/YYYY') FECHASOLICITUD, 
   	VALORTOTAL, 
   	NVL(PRIMERAPELLIDO,' ') ||' '|| NVL(SEGUNDOAPELLIDO, ' ') ||' '|| NVL(NOMBRES,' ') AS NOMBRECOMPLETO, 
    SS.CODIGORUTA,
    NVL(SUM(VALOR),0) AS SUMADEVALOR, 
    NVL(SUM(INICIAL),0) AS SUMADEINICIAL
FROM SP_SOLICITUDSERVICIO SS
	LEFT JOIN SP_SOLICITUDFINANCIABLES SF
		ON SS.COMPANIA 		  = SF.COMPANIA
		AND SS.CLASESOLICITUD = SF.CLASESOLICITUD
  		AND SS.NUMERO 		  = SF.SOLICITUD
GROUP BY SS.COMPANIA,
 	SS.CLASESOLICITUD,
  	NUMERO,
   	DIRTECNICA,
    NIT,
    FECHASOLICITUD,
    VALORTOTAL,
    NVL(PRIMERAPELLIDO,' ') ||' '|| NVL(SEGUNDOAPELLIDO, ' ') ||' '|| NVL(NOMBRES,' '), 
    SS.CODIGORUTA
HAVING SS.COMPANIA=s$compania$s 
	AND FECHASOLICITUD BETWEEN s$fechaInicial$s AND s$fechaFinal$s
ORDER BY NUMERO
