SELECT DEUDA.CODIGORUTA, 
  USUARIO.PRIMERAPELLIDO ||' '|| USUARIO.SEGUNDOAPELLIDO ||' '|| USUARIO.NOMBRES NOMBRECOMPLETO, 
  DEUDA.CONCEPTO,
  CONCEPTOS.NOMBRE NOMBRECONCEPTO,
  DEUDA.VALOR_FACTURADO,   
  DEUDA.DEUDA
FROM SP_D_DEUDAFACTURADAFINANCIADA DEUDA 
	LEFT JOIN SP_USUARIO USUARIO
		 ON DEUDA.COMPANIA   = USUARIO.COMPANIA
		AND DEUDA.CICLO      = USUARIO.CICLO
		AND DEUDA.CODIGORUTA = USUARIO.CODIGORUTA
	LEFT JOIN SP_CONCEPTOS CONCEPTOS
		 ON DEUDA.COMPANIA   = CONCEPTOS.COMPANIA
		AND DEUDA.CONCEPTO   = CONCEPTOS.CODIGO
WHERE DEUDA.COMPANIA = s$compania$s
s$filtro$s
AND (DEUDA.ANO || DEUDA.PERIODO) BETWEEN (s$anioInicial$s || 's$periodoInicial$s') AND (s$anioFinal$s || 's$periodoFinal$s')
AND (DEUDA.CONCEPTO BETWEEN 1 AND 49
OR   DEUDA.CONCEPTO IN (246,247,248,249)) 
ORDER BY DEUDA.CONCEPTO
