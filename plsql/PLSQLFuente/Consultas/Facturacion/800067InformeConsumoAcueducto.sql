WITH C1 AS (SELECT * FROM (SELECT SP_ESTADISTICAS_CONSUMO.COMPANIA,
    SP_TARIFAS.USOSUPERSERVICIOS,
    SP_ESTADISTICAS_CONSUMO.VCONSUMOACUEDUCTO,
    SP_ESTADISTICAS_CONSUMO.FACTURADOPROMEDIO,
    LPAD(SP_ESTADISTICAS_CONSUMO.ANO,4) || LPAD(SP_ESTADISTICAS_CONSUMO.PERIODO,2) AS PERI,
    CASE WHEN SP_TARIFAS.USOSUPERSERVICIOS = '1' THEN SP_ESTADISTICAS_CONSUMO.ESTRATO ELSE '1' END AS ESTRAT,
    CASE WHEN SP_TARIFAS.USOSUPERSERVICIOS = '1' THEN SP_ESTADISTICAS_CONSUMO.RANGO ELSE '00' END    AS RANGOS
  FROM SP_ESTADISTICAS_CONSUMO
  LEFT JOIN SP_TARIFAS
  ON SP_ESTADISTICAS_CONSUMO.COMPANIA    = SP_TARIFAS.COMPANIA
  AND SP_ESTADISTICAS_CONSUMO.ANO        = SP_TARIFAS.ANO
  AND SP_ESTADISTICAS_CONSUMO.PERIODO    = SP_TARIFAS.PERIODO
  AND SP_ESTADISTICAS_CONSUMO.USO        = SP_TARIFAS.USO
  AND SP_ESTADISTICAS_CONSUMO.ESTRATO    = SP_TARIFAS.ESTRATO
  WHERE SP_TARIFAS.USOSUPERSERVICIOS IS NOT NULL
  AND SP_ESTADISTICAS_CONSUMO.COMPANIA = s$compania$s
  AND LPAD(SP_ESTADISTICAS_CONSUMO.ANO,4)
    || LPAD(SP_ESTADISTICAS_CONSUMO.PERIODO,2) BETWEEN LPAD('s$anoInicial$s',4)
    || LPAD('s$periodoInicial$s',2) AND LPAD('s$anoFinal$s',4)
    || LPAD('s$periodoFinal$s',2)
  AND SP_ESTADISTICAS_CONSUMO.SERVICIO          = '01'
  GROUP BY SP_ESTADISTICAS_CONSUMO.COMPANIA,
    SP_TARIFAS.USOSUPERSERVICIOS,
    SP_ESTADISTICAS_CONSUMO.VCONSUMOACUEDUCTO,
    SP_ESTADISTICAS_CONSUMO.FACTURADOPROMEDIO,
    LPAD(SP_ESTADISTICAS_CONSUMO.ANO,4) || LPAD(SP_ESTADISTICAS_CONSUMO.PERIODO,2),
    CASE WHEN SP_TARIFAS.USOSUPERSERVICIOS = '1' THEN SP_ESTADISTICAS_CONSUMO.ESTRATO ELSE '1' END,
    CASE WHEN SP_TARIFAS.USOSUPERSERVICIOS = '1' THEN SP_ESTADISTICAS_CONSUMO.RANGO ELSE '00' END
  ORDER BY SP_ESTADISTICAS_CONSUMO.COMPANIA,
    SP_TARIFAS.USOSUPERSERVICIOS,
    SP_ESTADISTICAS_CONSUMO.VCONSUMOACUEDUCTO,
    SP_ESTADISTICAS_CONSUMO.FACTURADOPROMEDIO,
    LPAD(SP_ESTADISTICAS_CONSUMO.ANO,4) || LPAD(SP_ESTADISTICAS_CONSUMO.PERIODO,2),
    CASE WHEN SP_TARIFAS.USOSUPERSERVICIOS = '1' THEN SP_ESTADISTICAS_CONSUMO.ESTRATO ELSE '1' END,
    CASE WHEN SP_TARIFAS.USOSUPERSERVICIOS = '1' THEN SP_ESTADISTICAS_CONSUMO.RANGO ELSE '00' END)
    PIVOT (SUM(NVL(VCONSUMOACUEDUCTO,0) + NVL(FACTURADOPROMEDIO,0)) FOR PERI IN(s$inPivot$s))
    ORDER BY USOSUPERSERVICIOS,ESTRAT, RANGOS),    
C2 AS (SELECT * FROM (SELECT SP_ESTADISTICAS_CONSUMO.COMPANIA,
    SP_TARIFAS.USOSUPERSERVICIOS,
    SP_Estadisticas_Consumo.CargoFijoAcueducto,
    LPAD(SP_ESTADISTICAS_CONSUMO.ANO,4) || LPAD(SP_ESTADISTICAS_CONSUMO.PERIODO,2) AS PERI,
    CASE WHEN SP_TARIFAS.USOSUPERSERVICIOS = '1' THEN SP_ESTADISTICAS_CONSUMO.ESTRATO ELSE '1' END AS ESTRAT,
    CASE WHEN SP_TARIFAS.USOSUPERSERVICIOS = '1' THEN '00' ELSE 'ZZ' END    AS RANGOS
  FROM SP_ESTADISTICAS_CONSUMO
  LEFT JOIN SP_TARIFAS
  ON SP_ESTADISTICAS_CONSUMO.COMPANIA    = SP_TARIFAS.COMPANIA
  AND SP_ESTADISTICAS_CONSUMO.ANO        = SP_TARIFAS.ANO
  AND SP_ESTADISTICAS_CONSUMO.PERIODO    = SP_TARIFAS.PERIODO
  AND SP_ESTADISTICAS_CONSUMO.USO        = SP_TARIFAS.USO
  AND SP_ESTADISTICAS_CONSUMO.ESTRATO    = SP_TARIFAS.ESTRATO
  WHERE SP_TARIFAS.USOSUPERSERVICIOS IS NOT NULL
  AND SP_ESTADISTICAS_CONSUMO.COMPANIA = s$compania$s
  AND LPAD(SP_ESTADISTICAS_CONSUMO.ANO,4)
    || LPAD(SP_ESTADISTICAS_CONSUMO.PERIODO,2) BETWEEN LPAD('s$anoInicial$s',4)
    || LPAD('s$periodoInicial$s',2) AND LPAD('s$anoFinal$s',4)
    || LPAD('s$periodoFinal$s',2)
  AND SP_ESTADISTICAS_CONSUMO.SERVICIO          = '01'
  GROUP BY SP_ESTADISTICAS_CONSUMO.COMPANIA,
    SP_TARIFAS.USOSUPERSERVICIOS,
    SP_Estadisticas_Consumo.CargoFijoAcueducto,
    LPAD(SP_ESTADISTICAS_CONSUMO.ANO,4) || LPAD(SP_ESTADISTICAS_CONSUMO.PERIODO,2),
    CASE WHEN SP_TARIFAS.USOSUPERSERVICIOS = '1' THEN SP_ESTADISTICAS_CONSUMO.ESTRATO ELSE '1' END,
    CASE WHEN SP_TARIFAS.USOSUPERSERVICIOS = '1' THEN SP_ESTADISTICAS_CONSUMO.RANGO ELSE '00' END
  ORDER BY SP_ESTADISTICAS_CONSUMO.COMPANIA,
    SP_TARIFAS.USOSUPERSERVICIOS,
    SP_Estadisticas_Consumo.CargoFijoAcueducto,
    LPAD(SP_ESTADISTICAS_CONSUMO.ANO,4) || LPAD(SP_ESTADISTICAS_CONSUMO.PERIODO,2),
    CASE WHEN SP_TARIFAS.USOSUPERSERVICIOS = '1' THEN SP_ESTADISTICAS_CONSUMO.ESTRATO ELSE '1' END,
    CASE WHEN SP_TARIFAS.USOSUPERSERVICIOS = '1' THEN SP_ESTADISTICAS_CONSUMO.RANGO ELSE '00' END)
    PIVOT (SUM(CargoFijoAcueducto) FOR PERI IN(s$inPivot$s))
    ORDER BY USOSUPERSERVICIOS,ESTRAT, RANGOS)
    SELECT * FROM C1 UNION SELECT * FROM C2
