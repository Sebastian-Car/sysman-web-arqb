SELECT SP_USUARIO.ANO,
  SP_USUARIO.PERIODO,
  SP_USUARIO.USO,
  SP_USUARIO.ESTRATO,
  SP_USUARIO.CICLO,
  SP_USUARIO.DIRTECNICA,
  SP_USUARIO.DIRGUIA,
  SP_USUARIO.CODIGORUTA,
  SP_USUARIO.CODIGOINTERNO,
  SP_USUARIO.NIT,
  SP_USUARIO.PRIMERAPELLIDO
  || ' '
  || SP_USUARIO.SEGUNDOAPELLIDO
  || ' '
  || SP_USUARIO.NOMBRES AS NOMBRECOMP
FROM SP_USUARIO
INNER JOIN SP_FACTURADO
ON SP_USUARIO.CODIGORUTA          = SP_FACTURADO.CODIGORUTA
AND SP_USUARIO.CICLO              = SP_FACTURADO.CICLO
AND SP_USUARIO.COMPANIA           = SP_FACTURADO.COMPANIA
WHERE SP_USUARIO.COMPANIA         = s$compania$s
AND SP_FACTURADO.CONCEPTO        IN (24,19)
AND SP_FACTURADO.VALOR_FACTURADO <> 0
GROUP BY SP_USUARIO.ANO,
  SP_USUARIO.PERIODO,
  SP_USUARIO.USO,
  SP_USUARIO.ESTRATO,
  SP_USUARIO.CICLO,
  SP_USUARIO.DIRTECNICA,
  SP_USUARIO.DIRGUIA,
  SP_USUARIO.CODIGORUTA,
  SP_USUARIO.CODIGOINTERNO,
  SP_USUARIO.NIT,
  SP_USUARIO.PRIMERAPELLIDO
  || ' '
  || SP_USUARIO.SEGUNDOAPELLIDO
  || ' '
  || SP_USUARIO.NOMBRES
HAVING SP_USUARIO.CICLO= s$ciclo$s
ORDER BY SP_USUARIO.PRIMERAPELLIDO
  || ' '
  || SP_USUARIO.SEGUNDOAPELLIDO
  || ' '
  || SP_USUARIO.NOMBRES 
