SELECT CODIGORUTA, 
	CODIGOINTERNO, 
	NIT, 
	DIRGUIA, 
	USO, 
	LECTURA1, 
	LECTURA, 
	CONSUMOPROM, 
	CONSUMO, 
	DIRTECNICA,
	CICLO,
	NVL(PRIMERAPELLIDO,' ')||' '||NVL(SEGUNDOAPELLIDO,' ')||' '||NVL(NOMBRES,' ') NOMBRECOMPLETO
FROM SP_USUARIO
WHERE COMPANIA 	 =s$compania$s
	AND CICLO 	 =s$ciclo$s
	AND CONSUMO >=s$consumo$s
ORDER BY CODIGORUTA
