SELECT SP_USUARIO.CODIGORUTA,
       (PRIMERAPELLIDO||' '||SEGUNDOAPELLIDO||' '||NOMBRES) NOMBRECOMPLETO,
       SP_CONCEPTOS.CODIGO,
       SP_CONCEPTOS.NOMBRE NOMBRECONCEPTO,
       SP_FACTURADO.VALOR_FACTURADOANT,
       SP_FACTURADO.VALOR_FACTURADO,
       SP_FACTURADO.VALOR_FACTURADOIN,
       SP_FACTURADO.DEUDAANT,
       SP_FACTURADO.VALOR_FACTURADOANT+SP_FACTURADO.DEUDAANT TOTALANTERIOR,
       SP_FACTURADO.DEUDA,
       ROUND(SP_FACTURADO.VALOR_FACTURADO-SP_FACTURADO.VALOR_FACTURADOIN,2) AS DIFERENCIA,
       SP_USUARIO.COMPANIA,
       SP_FACTURADO.ANO||SP_FACTURADO.PERIODO PERIODOUNICO
  FROM SP_USUARIO
    LEFT JOIN SP_FACTURADO
       ON SP_USUARIO.PERIODO    = SP_FACTURADO.PERIODO
      AND SP_USUARIO.ANO        = SP_FACTURADO.ANO
      AND SP_USUARIO.COMPANIA   = SP_FACTURADO.COMPANIA
      AND SP_USUARIO.CICLO      = SP_FACTURADO.CICLO
      AND SP_USUARIO.CODIGORUTA = SP_FACTURADO.CODIGORUTA
      AND SP_USUARIO.CICLO      = SP_FACTURADO.CICLO
      AND SP_USUARIO.COMPANIA   = SP_FACTURADO.COMPANIA
    LEFT JOIN SP_CONCEPTOS
       ON SP_FACTURADO.COMPANIA = SP_CONCEPTOS.COMPANIA
      AND SP_FACTURADO.CONCEPTO = SP_CONCEPTOS.CODIGO  
 WHERE SP_USUARIO.COMPANIA = s$compania$s
   s$ciclo$s
   s$codigoRuta$s
   AND SP_FACTURADO.VALOR_FACTURADO <> SP_FACTURADO.VALOR_FACTURADOIN
   AND SP_FACTURADO.ANO||SP_FACTURADO.PERIODO BETWEEN s$periodoInicial$s AND s$periodoFinal$s
   AND SP_FACTURADO.CONCEPTO NOT IN (16,23,27,30,31,250)
