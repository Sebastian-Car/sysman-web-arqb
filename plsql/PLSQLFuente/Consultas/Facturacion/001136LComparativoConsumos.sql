WITH QRYDEUDAPERIODOANT AS (
SELECT USUARIO.COMPANIA,
  USUARIO.CICLO,
  USUARIO.CODIGORUTA, 
  USUARIO.CONSUMO, 
  USUARIO.FECHAPAGOPERANTERIOR, 
  USUARIO.FECHAPAGOPERPROCESO, 
  USUARIO.BANCOPERPROCESO, 
  USUARIO.BANCOPERANTERIOR, 
  USUARIO.CONSUMO1, 
  SUBSTR(USUARIO.CODIGORUTA,1,2) RUTA, 
  PRIMERAPELLIDO||' '||SEGUNDOAPELLIDO||' '||NOMBRES XX, 
  USUARIO.ANO,  
  USUARIO.PERIODO, 
  SUM(FACTURADO.VALOR_FACTURADO)    SUMADEVALOR_FACTURADO, 
  SUM(FACTURADO.DEUDA)              SUMADEDEUDA, 
  SUM(FACTURADO.VALOR_FACTURADOANT) SUMADEVALOR_FACTURADOANT, 
  SUM(FACTURADO.DEUDAANT)           SUMADEDEUDAANT 
FROM SP_USUARIO USUARIO 
 INNER JOIN SP_FACTURADO FACTURADO
  ON  USUARIO.COMPANIA   = FACTURADO.COMPANIA
  AND USUARIO.CICLO      = FACTURADO.CICLO 
  AND USUARIO.ANO        = FACTURADO.ANO
  AND USUARIO.PERIODO    = FACTURADO.PERIODO 
  AND USUARIO.CODIGORUTA = FACTURADO.CODIGORUTA 
WHERE USUARIO.COMPANIA    = s$compania$s
  AND USUARIO.CICLO       = s$ciclo$s
  AND USUARIO.CODIGORUTA  BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s
'
  AND USUARIO.ESTADO      NOT IN ('R')
  AND (FACTURADO.CONCEPTO BETWEEN 0 AND 49
  OR FACTURADO.CONCEPTO   IN (249,250))
GROUP BY USUARIO.COMPANIA, 
  USUARIO.CICLO,  
  USUARIO.CODIGORUTA, 
  USUARIO.CONSUMO, 
  USUARIO.FECHAPAGOPERANTERIOR, 
  USUARIO.FECHAPAGOPERPROCESO, 
  USUARIO.BANCOPERPROCESO,
  USUARIO.BANCOPERANTERIOR, 
  USUARIO.CONSUMO1, 
  SUBSTR(USUARIO.CODIGORUTA,1,2), 
  PRIMERAPELLIDO||' '||SEGUNDOAPELLIDO||' '||NOMBRES,  
  USUARIO.ANO, 
  USUARIO.PERIODO 
)
SELECT 
  QRYDEUDAPERIODOANT.CODIGORUTA, 
  QRYDEUDAPERIODOANT.SUMADEVALOR_FACTURADO,
  QRYDEUDAPERIODOANT.SUMADEVALOR_FACTURADOANT,
  QRYDEUDAPERIODOANT.SUMADEDEUDAANT,
  QRYDEUDAPERIODOANT.SUMADEDEUDA, 
  QRYDEUDAPERIODOANT.CICLO, 
  QRYDEUDAPERIODOANT.XX,
  QRYDEUDAPERIODOANT.CONSUMO, 
  QRYDEUDAPERIODOANT.CONSUMO1,
  SUMADEVALOR_FACTURADO-SUMADEDEUDA DIFACT, 
  SUMADEVALOR_FACTURADOANT-SUMADEDEUDAANT DIFANT, 
  (SUMADEVALOR_FACTURADO-SUMADEDEUDA)-(SUMADEVALOR_FACTURADOANT-SUMADEDEUDAANT) DIFTOTAL, 
  CONSUMO-CONSUMO1 DIFCONS  
FROM QRYDEUDAPERIODOANT 
WHERE ABS((SUMADEVALOR_FACTURADO-SUMADEDEUDA)-(SUMADEVALOR_FACTURADOANT-SUMADEDEUDAANT))>(SUMADEVALOR_FACTURADO-SUMADEDEUDA)*(s$porcentaje$s/100)
