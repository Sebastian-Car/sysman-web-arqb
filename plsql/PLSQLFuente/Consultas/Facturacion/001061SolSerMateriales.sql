SELECT 
  SP_SOLICITUDSERVICIO.COMPANIA, 
  SP_SOLICITUDSERVICIO.NUMERO, 
  SP_SOLICITUDSERVICIO.CODIGORUTA, 
  SP_SOLICITUDSERVICIO.NOMBRE, 
  SP_SOLICITUDSERVICIO.DIRTECNICA, 
  SP_SOLICITUDSERVICIO.NUMMEDIDOR, 
  SP_ELEMENTOSSOLICITUD.CODIGO, 
  SP_ELEMENTOS.DESCRIPCION, 
  SP_ELEMENTOSSOLICITUD.CANTIDAD, 
  SP_ELEMENTOSSOLICITUD.VALORUNITARIO, 
  SP_ELEMENTOSSOLICITUD.PORCIVA, 
  SP_SOLICITUDSERVICIO.OBS_PRESUPUESTP,
  ROUND(SP_ELEMENTOSSOLICITUD.CANTIDAD*SP_ELEMENTOSSOLICITUD.VALORUNITARIO*(1+SP_ELEMENTOSSOLICITUD.PORCIVA/100),2) AS TEXTOTOTAL
FROM SP_ELEMENTOSSOLICITUD 
  INNER JOIN SP_SOLICITUDSERVICIO 
     ON SP_ELEMENTOSSOLICITUD.COMPANIA = SP_SOLICITUDSERVICIO.COMPANIA
    AND SP_ELEMENTOSSOLICITUD.CLASESOLICITUD = SP_SOLICITUDSERVICIO.CLASESOLICITUD
    AND SP_ELEMENTOSSOLICITUD.SOLICITUD = SP_SOLICITUDSERVICIO.NUMERO
  INNER JOIN SP_ELEMENTOS 
     ON SP_ELEMENTOSSOLICITUD.COMPANIA = SP_ELEMENTOS.COMPANIA
    AND SP_ELEMENTOSSOLICITUD.CODIGO = SP_ELEMENTOS.CODIGO    
WHERE SP_SOLICITUDSERVICIO.COMPANIA =  s$compania$s
  AND SP_SOLICITUDSERVICIO.CLASESOLICITUD = 's$claseSolicitud$s'
  AND SP_SOLICITUDSERVICIO.NUMERO = s$numero$s
