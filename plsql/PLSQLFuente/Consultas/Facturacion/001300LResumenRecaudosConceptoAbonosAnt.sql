WITH DOBLESAGRUPSINUSUARIO AS (
  SELECT  SP_D_PAGOSDOBLES.COMPANIA
          ,SP_D_PAGOSDOBLES.FECHA
          ,SP_D_PAGOSDOBLES.BANCO
          ,SP_D_PAGOSDOBLES.NUMEROPAQUETE
          ,SP_D_PAGOSDOBLES.CONCEPTO
          ,SUM(SP_D_PAGOSDOBLES.VALOR) TOTALES
  FROM     SP_D_PAGOSDOBLES
  GROUP BY SP_D_PAGOSDOBLES.COMPANIA
          ,SP_D_PAGOSDOBLES.FECHA
          ,SP_D_PAGOSDOBLES.BANCO
          ,SP_D_PAGOSDOBLES.NUMEROPAQUETE
          ,SP_D_PAGOSDOBLES.CONCEPTO
)
SELECT   SUM(SP_D_RECAUDO.VALORPAGOPERIODO) SUMADEVALORPAGOPERIODO
        ,SP_D_RECAUDO.CONCEPTO
        ,SP_CONCEPTOS.NOMBRE NOMBRECONCEPTO
        ,SUM(SP_D_RECAUDO.VALORDEUDA) SUMADEVALORDEUDA
        ,SUM(SP_D_RECAUDO.VALORFINACT) SUMADEVALORFINACT
        ,SUM(SP_D_RECAUDO.VALORFINANT) SUMADEVALORFINANT
        ,SUM(NVL(DOBLESAGRUPSINUSUARIO.TOTALES,0)) DBLTOTALES
        ,SUM(SP_D_RECAUDO.CREDITOABONADO) SUMADECREDITOABONADO
        ,SUM(SP_D_RECAUDO.VALORABONOACT) SUMADEVALORABONOACT
        ,SUM(SP_D_RECAUDO.VALORABONOANT) SUMADEVALORABONOANT
FROM     SP_D_RECAUDO
  LEFT JOIN SP_CONCEPTOS
    ON      SP_D_RECAUDO.COMPANIA = SP_CONCEPTOS.COMPANIA
   AND      SP_D_RECAUDO.CONCEPTO = SP_CONCEPTOS.CODIGO
  LEFT JOIN DOBLESAGRUPSINUSUARIO
    ON      SP_D_RECAUDO.COMPANIA      = DOBLESAGRUPSINUSUARIO.COMPANIA
   AND      SP_D_RECAUDO.FECHA         = DOBLESAGRUPSINUSUARIO.FECHA
   AND      SP_D_RECAUDO.BANCO         = DOBLESAGRUPSINUSUARIO.BANCO
   AND      SP_D_RECAUDO.NUMEROPAQUETE = DOBLESAGRUPSINUSUARIO.NUMEROPAQUETE
   AND      SP_D_RECAUDO.CONCEPTO      = DOBLESAGRUPSINUSUARIO.CONCEPTO
WHERE    SP_D_RECAUDO.COMPANIA  = s$compania$s
  AND    SP_D_RECAUDO.FECHA     IS NOT NULL
  AND    SP_D_RECAUDO.FECHA     BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND    (SP_D_RECAUDO.CONCEPTO BETWEEN 1 AND 48
   OR    SP_D_RECAUDO.CONCEPTO  IN (201,202,203,204,205,206,207,246,247,248,249))
  AND    SP_D_RECAUDO.CONCEPTO  NOT IN (12,17)
GROUP BY SP_D_RECAUDO.CONCEPTO
        ,SP_CONCEPTOS.NOMBRE
HAVING SUM(SP_D_RECAUDO.VALORPAGOPERIODO
         + SP_D_RECAUDO.VALORDEUDA
         + NVL(SP_D_RECAUDO.VALORFINACT,0)
         + NVL(SP_D_RECAUDO.VALORFINANT,0)
         + SP_D_RECAUDO.VALORABONOANT
         + SP_D_RECAUDO.VALORABONOACT
         + NVL(DOBLESAGRUPSINUSUARIO.TOTALES,0)
         - SP_D_RECAUDO.CREDITOABONADO) <> 0
