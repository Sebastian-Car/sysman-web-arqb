SELECT SP_FINANCIABLES.COMPANIA, 
  SP_FINANCIABLES.CICLO, 
  SP_FINANCIABLES.CODIGORUTA, 
  SP_FINANCIABLES.ANO, 
  SP_FINANCIABLES.PERIODO, 
  SP_CONCEPTOS.NOMBRE CONCEPTO,
  SP_FINANCIABLES.MONTOFINANCIAR, 
  SP_FINANCIABLES.NUMEROCUOTAS, 
  SP_FINANCIABLES.SALDOFINANCIABLE, 
  SP_FINANCIABLES.VALORCUOTA, 
  SP_FINANCIABLES.INTERES, 
  SP_FINANCIABLES.NROCUOTA, 
  (SP_FINANCIABLES.ANO || SP_FINANCIABLES.PERIODO)  AS PERIODOANO, 
  (SP_USUARIO.NOMBRES || ' ' || SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO) AS NOMBREUSUARIO, 
  COMPANIA.NOMBRE, 
  SP_USUARIO.BANCOPERPROCESO,
  TO_CHAR(SP_USUARIO.FECHAPAGOPERPROCESO,'DD/MM/YYYY') FECHAPAGOPERPROCESO, 
  SP_USUARIO.PAQUETEPAGOPERPROCESO, 
  CASE WHEN SP_USUARIO.BANCOPERPROCESO IS NULL THEN 0 ELSE SP_FACTURADO.DEUDA END AS DEUDAS, 
  CASE WHEN SP_USUARIO.BANCOPERPROCESO IS NULL THEN 0 ELSE SP_FACTURADO.VALOR_FACTURADO END  AS VALORFACTURADO 
FROM SP_FINANCIABLES  
LEFT JOIN SP_FACTURADO  
  ON  SP_FINANCIABLES.COMPANIA = SP_FACTURADO.COMPANIA  
  AND SP_FINANCIABLES.CICLO = SP_FACTURADO.CICLO 
  AND SP_FINANCIABLES.CODIGORUTA = SP_FACTURADO.CODIGORUTA 
  AND SP_FINANCIABLES.ANO = SP_FACTURADO.ANO  
  AND SP_FINANCIABLES.PERIODO = SP_FACTURADO.PERIODO  
  AND SP_FINANCIABLES.CONCEPTO = SP_FACTURADO.CONCEPTO 
LEFT JOIN SP_CONCEPTOS
  ON  SP_FINANCIABLES.COMPANIA = SP_CONCEPTOS.COMPANIA  
  AND SP_FINANCIABLES.CONCEPTO = SP_CONCEPTOS.CODIGO 
LEFT JOIN SP_USUARIO  
  ON  SP_FINANCIABLES.COMPANIA = SP_USUARIO.COMPANIA 
  AND SP_FINANCIABLES.CICLO = SP_USUARIO.CICLO 
  AND SP_FINANCIABLES.CODIGORUTA = SP_USUARIO.CODIGORUTA  
LEFT JOIN COMPANIA  
  ON  SP_FINANCIABLES.COMPANIA = COMPANIA.CODIGO 
WHERE SP_FINANCIABLES.COMPANIA='s$compania$s' 
  AND SP_FINANCIABLES.CICLO='s$ciclo$s' 
  AND SP_FINANCIABLES.CODIGORUTA  
  BETWEEN 's$codigoInicial$s' 
  AND 's$codigoFinal$s' 
  AND SP_FINANCIABLES.CONCEPTO  
  BETWEEN 's$conceptoInicial$s' 
  AND 's$conceptoFinal$s'
  AND (SP_FINANCIABLES.ANO || SP_FINANCIABLES.PERIODO) 
  BETWEEN 's$periodoInicialCompleto$s' 
  AND 's$periodoFinalCompleto$s'
