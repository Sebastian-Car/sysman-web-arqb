SELECT s$distinto$s USUARIO.CODIGORUTA,
  USUARIO.CODIGOINTERNO,
  USUARIO.NIT,
  USUARIO.PRIMERAPELLIDO || ' ' || USUARIO.SEGUNDOAPELLIDO || ' ' || USUARIO.NOMBRES AS NOMBRE,
  USUARIO.DIRTECNICA,
  USUARIO.DIRGUIA,
  USUARIO.USO,
  USUARIO.LECTURA1,
  USUARIO.LECTURA,
  USUARIO.CONSUMOPROM,
  USUARIO.CONSUMO,
  USUARIO.ACUMULADO, 
  s$problema$s
FROM SP_USUARIO USUARIO 
  INNER JOIN (SP_PROBLEMA PROBLEMA 
              INNER JOIN SP_USUARIO_PROBLEMA USUARIO_PROBLEMA 
                 ON PROBLEMA.CLASEPROBLEMA = USUARIO_PROBLEMA.CLASE 
                AND PROBLEMA.CODIGO        = USUARIO_PROBLEMA.PROBLEMA
                AND PROBLEMA.COMPANIA      = USUARIO_PROBLEMA.COMPANIA) 
   ON USUARIO.CODIGORUTA   = USUARIO_PROBLEMA.CODIGORUTA  
  AND USUARIO.CICLO        = USUARIO_PROBLEMA.CICLO 
  AND USUARIO.COMPANIA     = USUARIO_PROBLEMA.COMPANIA 
  AND USUARIO.ANO          = USUARIO_PROBLEMA.ANO
  AND USUARIO.PERIODO     = USUARIO_PROBLEMA.PERIODO 
WHERE USUARIO.COMPANIA      = s$compania$s
  AND USUARIO.CICLO         = s$ciclo$s
  AND USUARIO.TIPOCALCULO   = 'P'
  s$condicionEstado$s 
ORDER BY Usuario.CODIGORUTA
