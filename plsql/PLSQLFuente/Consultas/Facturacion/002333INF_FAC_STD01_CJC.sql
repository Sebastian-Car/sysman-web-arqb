SELECT
    SF_FACTURA.TIPO_FACTURA,
    SF_FACTURA.NUMERO_FACTURA,
    SF_FACTURA.TIPOCOBRO,
    SF_FACTURA.CODIGO_COBRO,
    TERCERO.NOMBRE,
    SF_FACTURA.TERCERO,
    TERCERO.DIRECCION,
    TERCERO.TELEFONOS,
    SF_FACTURA.SUCURSAL,
    SF_FACTURA.FECHA_EXPEDICION,
    SF_FACTURA.FECHA_VENCIMIENTO,
    SF_FACTURA.VALOR_TOTAL,
    SF_FACTURA.OBSERVACIONES,
    SF_DETALLE_FACTURA.ANO,
    SF_DETALLE_FACTURA.CONCEPTO,
    SF_CONCEPTOS.NOMBRE NOMBRECONCEPTO,
    SF_DETALLE_FACTURA.CANTIDAD,
    SF_DETALLE_FACTURA.VALOR_NETO,
    SF_DETALLE_FACTURA.FECHA_INICIO_COBRO,
    SF_DETALLE_FACTURA.FECHA_FIN_COBRO,
    ANULADA AS Expr1,
    SF_FACTURA.FECHA_PAGO,
    SF_TIPO_COBRO.NORES_FACTURACION,
    SF_TIPO_COBRO.FECRES_FACTURACION,
    SF_TIPO_COBRO.NOINICIAL_FACTURACION,
    SF_TIPO_COBRO.NOFINAL_FACTURACION,
    SF_DETALLE_FACTURA.VALOR_BASE,
    SF_DETALLE_FACTURA.VALOR_IVA,
    SF_DETALLE_FACTURA.VALOR_RETEFUENTE,
    SF_FACTURA.REF_FACTURACION
FROM SF_DETALLE_FACTURA
    INNER JOIN SF_FACTURA
    ON SF_DETALLE_FACTURA.FACTURA = SF_FACTURA.NUMERO_FACTURA
    AND SF_DETALLE_FACTURA.TIPO_FACTURA = SF_FACTURA.TIPO_FACTURA
    AND SF_DETALLE_FACTURA.COMPANIA = SF_FACTURA.COMPANIA
    
    INNER JOIN SF_TIPO_COBRO
    ON SF_FACTURA.COMPANIA = SF_TIPO_COBRO.COMPANIA
    AND SF_FACTURA.TIPO_FACTURA = SF_TIPO_COBRO.CODIGO
    AND SF_DETALLE_FACTURA.ANO = SF_TIPO_COBRO.ANO
    
    INNER JOIN TERCERO 
    ON SF_FACTURA.COMPANIA = TERCERO.COMPANIA
    AND SF_FACTURA.TERCERO = TERCERO.NIT
    AND SF_FACTURA.SUCURSAL = TERCERO.SUCURSAL
    
    INNER JOIN SF_CONCEPTOS 
    ON SF_CONCEPTOS.COMPANIA = SF_DETALLE_FACTURA.COMPANIA
    AND SF_CONCEPTOS.ANO = SF_DETALLE_FACTURA.ANO
    AND SF_CONCEPTOS.TIPOCOBRO = SF_DETALLE_FACTURA.TIPOCOBRO
    AND SF_CONCEPTOS.CODIGO = SF_DETALLE_FACTURA.CONCEPTO
WHERE  SF_FACTURA.COMPANIA =  s$compania$s AND SF_FACTURA.TIPO_FACTURA   = 's$tipoFactura$s'
    AND SF_FACTURA.NUMERO_FACTURA BETWEEN s$facturaInicial$s AND s$facturaFinal$s --And ((Nz([anulada],0 )) = 0 )
    AND SF_FACTURA.fecha_pago IS NULL
      AND ANULADA IN (0)