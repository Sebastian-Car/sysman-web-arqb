SELECT U.CODIGORUTA, 
	NVL(U.PRIMERAPELLIDO,' ')||' '||NVL(U.SEGUNDOAPELLIDO,' ')||' '||NVL(U.NOMBRES,' ') NOMBRECOMPLETO,
	U.CODIGOINTERNO,
	A.VALOR AS TOTAL_FACTURA,
	ROUND(U.TOTFACTURAPERACTUAL*s$retencion$s/100) RETENCION,
	U.TOTFACTURAPERACTUAL+ROUND(U.TOTFACTURAPERACTUAL*s$retencion$s/100) AS TOTALRET,
	U.USO,
	U.ESTRATO
FROM SP_USUARIO U
	INNER JOIN SP_ABONOS A
		ON U.COMPANIA 	 = A.COMPANIA
		AND U.CICLO 	 = A.CICLO
		AND U.CODIGORUTA = A.CODIGORUTA
		AND U.ANO 		 = A.ANO
		AND U.PERIODO 	 = A.PERIODO
WHERE U.COMPANIA=s$compania$s
	s$condicionCiclo$s
	AND U.ESTADO NOT IN ('R','S') 
	s$condicionUso$s
ORDER BY U.CICLO
