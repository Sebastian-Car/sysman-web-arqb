SELECT  SP_TARIFAS.ASEOUNICO, 
        SP_TARIFAS.SUBASEOUNICO, 
        SP_TARIFAS.SOBREASEOUNICO, 
        SP_TARIFAS.ASEODOMICILIARIO, 
        SP_TARIFAS.SUBASEODOMICILIARIO, 
        SP_TARIFAS.SOBREASEODOMICILIARIO, 
        SP_TARIFAS.ASEOBARRIDO, 
        SP_TARIFAS.SUBASEOBARRIDO, 
        SP_TARIFAS.SOBREASEOBARRIDO, 
        SP_TARIFAS.ASEOCONSUMO, 
        SP_TARIFAS.SUBASEOCONSUMO, 
        SP_TARIFAS.SOBREASEOCONSUMO, 
        SP_TARIFAS.TRAMOEXCEDENTE, 
        SP_TARIFAS.SUBTRAMOEX, 
        SP_TARIFAS.SOBRETRAMOEX, 
        SP_TARIFAS.ASEODESHABITADOS, 
        SP_TARIFAS.DESCSEPARACIONENFUENTE, 
        SP_TARIFAS.DESCNOPUERTAPUERTA, 
        SP_TARIFAS.TDI, 
        SP_TARIFAS.USO, 
        SP_TARIFAS.ESTRATO, 
        SP_USOS.NOMBRE AS NOMBREUSO, 
        SP_ESTRATOS.NOMBRE AS NOMBREESTRATO 
FROM  (SP_TARIFAS 
        INNER JOIN SP_USOS 
          ON  (SP_TARIFAS.COMPANIA  = SP_USOS.COMPANIA 
          AND SP_TARIFAS.USO        =SP_USOS.CODIGO)) 
          INNER JOIN SP_ESTRATOS 
            ON SP_TARIFAS.COMPANIA = SP_ESTRATOS.COMPANIA 
            AND SP_TARIFAS.USO      = SP_ESTRATOS.USO 
            AND SP_TARIFAS.ESTRATO  = SP_ESTRATOS.CODIGO 
WHERE SP_TARIFAS.COMPANIA     =s$compania$s 
      AND SP_TARIFAS.ANO      =s$ano$s  
      AND SP_TARIFAS.PERIODO  ='s$mes$s' 
ORDER BY  SP_TARIFAS.COMPANIA, 
          SP_TARIFAS.ANO, 
          SP_TARIFAS.PERIODO, 
          SP_TARIFAS.USO, 
          SP_TARIFAS.ESTRATO
