SELECT ERR.CODIGORUTA,
  ERR.CODIGOERROR,
  ERR.MENSAJE,
  ERR.FECHA,
  US.PRIMERAPELLIDO || ' ' || US.SEGUNDOAPELLIDO || ' '
  || US.NOMBRES NOMBRE
FROM SP_USUARIO US
INNER JOIN SP_ERRORCALCULO ERR
ON US.COMPANIA    = ERR.COMPANIA
AND US.CICLO      = ERR.CICLO
AND US.CODIGORUTA = ERR.CODIGORUTA
WHERE  US.COMPANIA   = 's$compania$s'
  AND  US.CICLO      = 's$ciclo$s'
ORDER BY ERR.CODIGORUTA
