SELECT 
CODIGORUTA,
PRIMERAPELLIDO||' '||SEGUNDOAPELLIDO||' '||NOMBRES AS NOMBREUSUARIO,
DIRTECNICA,
DIRGUIA,
USO,
LECTURA1,
LECTURA,
CONSUMO,
LECTURAAFORO,
CONSUMOPROM,
CODIGOINTERNO,
MEDIDOR,
ACUMULADO,
  CASE WHEN   SP_USUARIO.CONSUMO-(CONSUMOPROM*(1+(s$limiteSuperior$s/100)))>=0 
    THEN 'Superior al promedio'
  ELSE 
    CASE WHEN   SP_USUARIO.CONSUMO-(CONSUMOPROM*(1-(s$limiteInferior$s/100)))<=0
      THEN 'Inferior al promedio'
    ELSE 
      CASE WHEN (SP_USUARIO.CONSUMO>=500 AND SP_USUARIO.USO='01') OR (SP_USUARIO.CONSUMO>=9000 AND SP_USUARIO.USO<>'01')
        THEN 'Elevado'
       ELSE ''
       END
     END
   END AS OBSERVACIONES ,
   SP_USUARIO.CONSUMO-(CONSUMOPROM*(1+(s$limiteSuperior$s/100))) AS DIFSUPERIORAPROMEDIO,
   SP_USUARIO.CONSUMO-(CONSUMOPROM*(1-(s$limiteInferior$s/100))) AS DIFINFERIORAPROMEDIO,
  1 AS CNSAF
FROM SP_USUARIO
WHERE SP_USUARIO.COMPANIA= s$compania$s
AND SP_USUARIO.CICLO      =s$ciclo$s
AND SP_USUARIO.CONSUMO    >0
AND (SP_USUARIO.CONSUMO   >=(CONSUMOPROM*(1+(s$limiteSuperior$s/100)))
OR SP_USUARIO.CONSUMO     <=(CONSUMOPROM*(1-(s$limiteInferior$s/100)))
)

OR (SP_USUARIO.COMPANIA   =s$compania$s
AND SP_USUARIO.CICLO      =s$ciclo$s
AND SP_USUARIO.CONSUMO   >=500
AND SP_USUARIO.USO        ='01')

OR (SP_USUARIO.COMPANIA   =s$compania$s
AND SP_USUARIO.CICLO      =s$ciclo$s
AND SP_USUARIO.CONSUMO   >=9000
AND SP_USUARIO.USO        ='01')
ORDER BY SP_USUARIO.COMPANIA,
  SP_USUARIO.CICLO,
  SP_USUARIO.CODIGORUTA
