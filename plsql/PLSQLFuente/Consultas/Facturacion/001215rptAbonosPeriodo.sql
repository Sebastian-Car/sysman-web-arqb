SELECT SP_ABONOS.CODIGORUTA, 
  SP_ABONOS.ANO, 
  TO_CHAR(SP_ABONOS.FECHA,'DD/MM/YYYY')FECHA, 
  SP_ABONOS.PERIODO AS PERCOMPLETO, 
  SP_ABONOS.VALOR    AS VLRTOTAL, 
  (PRIMERAPELLIDO || ' ' || SEGUNDOAPELLIDO || ' ' || NOMBRES) AS NOMBRECOMPLETO 
FROM SP_ABONOS 
LEFT JOIN SP_USUARIO 
ON SP_ABONOS.COMPANIA    = SP_USUARIO.COMPANIA 
AND SP_ABONOS.CICLO      = SP_USUARIO.CICLO 
AND SP_ABONOS.CODIGORUTA = SP_USUARIO.CODIGORUTA 
WHERE SP_ABONOS.COMPANIA= 's$compania$s' 
  s$condicionCiclo$s 
  AND (SP_ABONOS.ANO|| SP_ABONOS.PERIODO)  
  BETWEEN 's$periodoInicialEnvio$s'  AND 's$periodoFinalEnvio$s'  
  AND SP_ABONOS.CODIGORUTA 
  BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 
  AND SP_ABONOS.FECHA 
  BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND  TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
