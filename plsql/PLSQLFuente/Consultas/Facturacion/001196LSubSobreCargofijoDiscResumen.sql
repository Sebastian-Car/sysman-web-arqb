SELECT SP_USUARIO.CICLO, 
       SP_ESTRATOS.NOMBRE ESTRATO,
       SP_USOS.NOMBRE USO,
       SUM(SP_USUARIO.SUBFIJO) SUBFIJO, 
       SUM(SP_USUARIO.SOBREFIJO) SOBREFIJO,
       SUM(SP_USUARIO.CARGOFIJO) CARGOFIJO,
       SUM(CASE WHEN SP_USUARIO.CARGOFIJO=0 AND FACTURADOCONCEPTO12.FINAN=-1 THEN FACTURADOCONCEPTO12.ACUEDUCTO ELSE 0 END) OTROSCARGOFIJO,
      (SUM(SP_USUARIO.CARGOFIJO) + SUM(CASE WHEN SP_USUARIO.CARGOFIJO=0 AND FACTURADOCONCEPTO12.FINAN=-1 THEN FACTURADOCONCEPTO12.ACUEDUCTO ELSE 0 END))-(SUM(SP_USUARIO.SOBREFIJO)-SUM(SP_USUARIO.SUBFIJO) )  TOTALACUEDUCTO,
       SUM(SP_USUARIO.CARGOFIJOAL) CARGOFIJOAL,
       SUM(CASE WHEN SP_USUARIO.CARGOFIJOAL=0 AND FACTURADOCONCEPTO12.FINAN=-1 THEN FACTURADOCONCEPTO12.ALCANTARILLADO ELSE 0 END) OTROSCARGOFIJOAL, 
       SUM(SP_USUARIO.SUBFIJOALC) SUBFIJOALC,
       SUM(SP_USUARIO.SOBREFIJOALC) SOBREFIJOALC,
       (SUM(SP_USUARIO.CARGOFIJOAL)+SUM(CASE WHEN SP_USUARIO.CARGOFIJOAL=0 AND FACTURADOCONCEPTO12.FINAN=-1 THEN FACTURADOCONCEPTO12.ALCANTARILLADO ELSE 0 END))-(SUM(SP_USUARIO.SOBREFIJOALC)-SUM(SP_USUARIO.SUBFIJOALC)) TOTALALCANTARILLADO,
       SP_USUARIO.ANO,
       SP_USUARIO.PERIODO,
       PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(SP_USUARIO.COMPANIA,SP_USUARIO.ANO,SP_USUARIO.PERIODO,NULL) NOMPERIODO
  FROM (SELECT SP_FACTURADO.COMPANIA,
               SP_FACTURADO.CICLO, 
               SP_FACTURADO.CODIGORUTA,
               SUM(CASE WHEN CONCEPTO=1 THEN VALOR_FACTURADOIN ELSE 0 END) ACUEDUCTO,
               SUM(CASE WHEN CONCEPTO=4 THEN VALOR_FACTURADOIN ELSE 0 END) ALCANTARILLADO,
               CASE WHEN SUM(SP_FACTURADO.VALOR_FACTURADO) = SUM(CASE WHEN CONCEPTO=12 THEN VALOR_FACTURADO ELSE 0 END) AND (SUM(CASE WHEN CONCEPTO=12 THEN VALOR_FACTURADO ELSE 0 END ))<>0 THEN -1 ELSE 0 END FINAN
          FROM SP_FACTURADO
         WHERE SP_FACTURADO.COMPANIA = s$compania$s
        GROUP BY SP_FACTURADO.COMPANIA, SP_FACTURADO.CICLO, SP_FACTURADO.CODIGORUTA)FACTURADOCONCEPTO12 
    INNER JOIN SP_USUARIO 
       ON FACTURADOCONCEPTO12.CODIGORUTA = SP_USUARIO.CODIGORUTA
      AND FACTURADOCONCEPTO12.CICLO = SP_USUARIO.CICLO
      AND FACTURADOCONCEPTO12.COMPANIA = SP_USUARIO.COMPANIA
    INNER JOIN SP_ESTRATOS
       ON SP_USUARIO.ESTRATO = SP_ESTRATOS.CODIGO
      AND SP_USUARIO.USO = SP_ESTRATOS.USO
      AND SP_USUARIO.COMPANIA = SP_ESTRATOS.COMPANIA
    INNER JOIN SP_USOS 
       ON SP_ESTRATOS.USO = SP_USOS.CODIGO
      AND SP_ESTRATOS.COMPANIA = SP_USOS.COMPANIA
 WHERE SP_USUARIO.COMPANIA = s$compania$s
   AND SP_USUARIO.CICLO BETWEEN s$cicloInicial$s AND s$cicloFinal$s
   AND SP_USUARIO.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 
   AND SP_USUARIO.ESTADO <> 'R' 
   AND (SP_USUARIO.ALCANTARILLADO <> 0 OR SP_USUARIO.ACUEDUCTO <> 0)
GROUP BY SP_USUARIO.CICLO, SP_ESTRATOS.NOMBRE, SP_USOS.NOMBRE, SP_USUARIO.ANO, SP_USUARIO.PERIODO, SP_USUARIO.ALCANTARILLADO, SP_USUARIO.COMPANIA
