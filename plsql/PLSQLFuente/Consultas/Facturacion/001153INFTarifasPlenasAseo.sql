SELECT SP_USUARIO.COMPANIA,
       SP_USUARIO.CICLO,
       SP_USUARIO.CODIGORUTA,
       SP_USUARIO.CODIGOINTERNO, 
       (SP_USUARIO.NOMBRES||' '||SP_USUARIO.PRIMERAPELLIDO||' '||SP_USUARIO.SEGUNDOAPELLIDO) NOMBRECOMPLETO,
       SP_USUARIO.DIRTECNICA,
       SP_USUARIO.NIT,
       PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(SP_USUARIO.COMPANIA,SP_USUARIO.ANO, SP_USUARIO.PERIODO,PCK_SYSMAN_UTL.FC_PAR(SP_USUARIO.COMPANIA,'FRECUENCIA PERIODOS DE FACTURACION','74',SYSDATE)) PER,
       SP_USUARIO.USO,
       SP_USUARIO.ESTRATO,
       SP_USUARIO.ESTRATOASEO, 
       SP_USUARIO.VASEOUNICO,
       SP_USUARIO.SUBBARR_LIM,
       SP_USUARIO.SOBBARR_LIM,
       SP_USUARIO.VASEODOMICILIARIO,
       SP_USUARIO.SUBDISP_FINAL,
       SP_USUARIO.SOBDISP_FINAL,
       SP_USUARIO.VASEOBARRIDO,
       SP_USUARIO.SUBMANEJO_REC, 
       SP_USUARIO.SOBMANEJO_REC,
       SP_USUARIO.VASEOCONSUMO,
       SP_USUARIO.SUBRECOLECCION, 
       SP_USUARIO.SOBRECOLECCION,
       SP_USUARIO.VDESHABITADO VALDESHABITADO,
       SP_USUARIO.SUBDESHABITADO, 
       SP_USUARIO.SOBDESHABITADO,
       SP_USUARIO.SUBASEO,
       SP_USUARIO.SOBREASEO,
       SP_USUARIO.PESOASEO,
       SP_USUARIO.VASEOUNICO+SP_USUARIO.VASEODOMICILIARIO+SP_USUARIO.VASEOBARRIDO+SP_USUARIO.VASEOCONSUMO+SP_USUARIO.VDESHABITADO+SP_USUARIO.VTRAMOEXCEDENTE TOTAL,
       SP_USUARIO.VTRAMOEXCEDENTE TRAMOEXCEDENTE,
       SP_USUARIO.SUBTRAMO,
       SP_USUARIO.SOBTRAMO
,
       SP_USUARIO.ANO,
       SP_USUARIO.PERIODO,
       SP_USUARIO.INDDESHABITADO,
       0 TRAMOEXCEDENTE,
       0 PLENA
  FROM SP_USUARIO
 WHERE SP_USUARIO.COMPANIA = s$compania$s
   AND SP_USUARIO.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
   AND SP_USUARIO.CICLO BETWEEN s$cicloInicial$s AND s$cicloFinal$s
   AND SP_USUARIO.ESTADO <> 'R' 
   AND SP_USUARIO.ASEO NOT IN (0)
