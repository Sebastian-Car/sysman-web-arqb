WITH QRY_CHAPETAS_ULTFECHA AS ( 
SELECT  QRY_CHAPETAS_ULT.COMPANIA, 
        QRY_CHAPETAS_ULT.CICLO, 
        QRY_CHAPETAS_ULT.CODIGORUTA, 
        SP_AUDITORIA_USUARIO.CHAPETAS, 
        SP_AUDITORIA_USUARIO.DATE_MODIFIED 
FROM (SELECT  SP_AUDITORIA_USUARIO.COMPANIA, 
              SP_AUDITORIA_USUARIO.CICLO, 
              SP_AUDITORIA_USUARIO.CODIGORUTA, 
              MAX(SP_AUDITORIA_USUARIO.CONSECUTIVO) AS CONSECUTIVO, 
              MAX(SP_AUDITORIA_USUARIO.DATE_MODIFIED) AS FECHA 
      FROM SP_AUDITORIA_USUARIO 
      WHERE 
            SP_AUDITORIA_USUARIO.CHAPETAS <> SP_AUDITORIA_USUARIO.CHAPETAS_ANT 
      GROUP BY 
            SP_AUDITORIA_USUARIO.COMPANIA, 
            SP_AUDITORIA_USUARIO.CICLO, 
            SP_AUDITORIA_USUARIO.CODIGORUTA 
      ORDER BY MAX(SP_AUDITORIA_USUARIO.DATE_MODIFIED) DESC) QRY_CHAPETAS_ULT 
INNER JOIN SP_AUDITORIA_USUARIO 
ON    QRY_CHAPETAS_ULT.COMPANIA      = SP_AUDITORIA_USUARIO.COMPANIA 
AND   QRY_CHAPETAS_ULT.CICLO         = SP_AUDITORIA_USUARIO.CICLO 
AND   QRY_CHAPETAS_ULT.CODIGORUTA    = SP_AUDITORIA_USUARIO.CODIGORUTA 
AND   QRY_CHAPETAS_ULT.CONSECUTIVO   = SP_AUDITORIA_USUARIO.CONSECUTIVO 
WHERE SP_AUDITORIA_USUARIO.CHAPETAS  = -1) 
SELECT  SP_USUARIO.COMPANIA, 
        SP_USUARIO.CICLO, 
        SP_USUARIO.CODIGORUTA, 
        SP_USUARIO.CODIGOINTERNO, 
        SP_USUARIO.ANO, 
        SP_USUARIO.PERIODO, 
        SP_ESTADOSCOBRO.DESCRIPCION AS TIPOCOBRO, 
        TRIM(SP_USUARIO.PRIMERAPELLIDO 
        || ' ' 
        || SP_USUARIO.SEGUNDOAPELLIDO 
        || ' ' 
        || SP_USUARIO.NOMBRES) AS NOMBRES , 
        SP_USUARIO.PERIODOSATRASO, 
        SP_USUARIO.MEDIDOR, 
        CASE (SP_USUARIO.ESTADO) 
          WHEN 'A' THEN 'Activo' 
          WHEN 'S' THEN 'Suspendido' 
          WHEN 'C' THEN 'Cortado' 
          WHEN 'R' THEN 'Retirado' 
        END AS ESTADO, 
        CASE WHEN SP_USUARIO.CHAPETAS = -1 THEN 'SI' ELSE 'NO' END CHAPETAS,
        SP_USUARIO.LECTURA1, 
        SP_USUARIO.TOTALDEUDA, 
        QRY_CHAPETAS_ULTFECHA.DATE_MODIFIED 
FROM SP_USUARIO INNER JOIN SP_ESTADOSCOBRO 
       ON SP_USUARIO.COMPANIA  = SP_ESTADOSCOBRO.COMPANIA 
      AND SP_USUARIO.TIPOCOBRO = SP_ESTADOSCOBRO.CODIGO 
      LEFT JOIN QRY_CHAPETAS_ULTFECHA 
      ON SP_USUARIO.CODIGORUTA = QRY_CHAPETAS_ULTFECHA.CODIGORUTA AND SP_USUARIO.CICLO = QRY_CHAPETAS_ULTFECHA.CICLO 
    AND  SP_USUARIO.COMPANIA = QRY_CHAPETAS_ULTFECHA.COMPANIA 
WHERE SP_USUARIO.COMPANIA            = s$compania$s 
AND SP_USUARIO.CICLO                 = 's$ciclo$s' 
AND NVL(SP_USUARIO.TIPOCOBRO,'NNN') IN (s$estados$s) 
AND SP_USUARIO.ESTADO NOT           IN ('R', 'S')
