SELECT USUARIO.CODIGORUTA,
  USUARIO.CODIGOINTERNO,
  (USUARIO.NOMBRES
  || ' '
  || USUARIO.PRIMERAPELLIDO
  || ' '
  || USUARIO.SEGUNDOAPELLIDO)                                                  AS NOMBRES,
  SUM(FACTURADO.VALOR_FACTURADO+FACTURADO.VALORFINACT+FACTURADO.VALORABONOACT) AS TOTALFACTURADO,
  CASE
    WHEN SUM(FACTURADO.VALOR_FACTURADO  + FACTURADO.VALORFINACT + FACTURADO.VALORABONOACT) >= 107000
    THEN (SUM(FACTURADO.VALOR_FACTURADO + FACTURADO.VALORFINACT + FACTURADO.VALORABONOACT) * 1.5)/100
    ELSE 0
  END CREE FROM SP_TARIFAS TARIFAS
INNER JOIN (SP_CONCEPTOS CONCEPTOS
INNER JOIN (SP_USUARIO USUARIO
INNER JOIN SP_FACTURADO FACTURADO
ON USUARIO.PERIODO             = FACTURADO.PERIODO
AND USUARIO.ANO                = FACTURADO.ANO
AND USUARIO.COMPANIA           = FACTURADO.COMPANIA
AND USUARIO.CICLO              = FACTURADO.CICLO
AND USUARIO.CODIGORUTA         = FACTURADO.CODIGORUTA)
ON CONCEPTOS.CODIGO            = FACTURADO.CONCEPTO
AND CONCEPTOS.COMPANIA         = FACTURADO.COMPANIA)
ON TARIFAS.PERIODO             = USUARIO.PERIODO
AND TARIFAS.ANO                = USUARIO.ANO
AND TARIFAS.COMPANIA           = USUARIO.COMPANIA
AND TARIFAS.USO                = USUARIO.USO
AND TARIFAS.ESTRATO            = USUARIO.ESTRATO
WHERE USUARIO.COMPANIA = s$compania$s 
AND TARIFAS.USOSUPERSERVICIOS IN ('2','3','4') 
s$condicionCiclo$s 
GROUP BY USUARIO.CODIGORUTA,
  USUARIO.CODIGOINTERNO,
  (USUARIO.NOMBRES
  || ' '
  || USUARIO.PRIMERAPELLIDO
  || ' '
  || USUARIO.SEGUNDOAPELLIDO)s$agrupamiento$s 
ORDER BY USUARIO.CODIGORUTA
