SELECT   
 NVL(RECARGOACUEDUCTO,0)+NVL(RECARGOASEO,0) TOTALRECARGO, 
 TOTFACTURAPERACTUAL-TOTALDEUDA-TOTALASEO-TOTALALCANTARILLADO-TOTALACUEDUCTO OTROSCARGOS,
 VALCONSUMO+VALCONSUMOPROM TOTALVALORCONSUMO, 
 VALCONSUMO+VALCONSUMOPROM-(SUBACUEDUCTO-SUBFIJO+(SOBREACUEDUCTO-SOBREFIJO)) TOTCONSUMO,
 CARGOFIJO+SUBFIJO-SOBREFIJO TOTFIJO, 
 USUARIO.VALCONSUMO VC, 
 USUARIO.VALCONSUMOPROM VCP, 
 USUARIO.CODIGOINTERNO,
 USUARIO.CODIGORUTA, 
 USUARIO.USO, 
 USUARIO.ESTRATO, 
 USUARIO.VASEOUNICO, 
 USUARIO.VASEODOMICILIARIO, 
 USUARIO.VASEOBARRIDO, 
 USUARIO.VASEOCONSUMO,
 USUARIO.SUBASEO, 
 USUARIO.SOBREASEO, 
 USUARIO.TOTALASEO, 
 USUARIO.PRIMERAPELLIDO, 
 USUARIO.SEGUNDOAPELLIDO, 
 USUARIO.NOMBRES, 
 USUARIO.PESOASEO, 
 USUARIO.ESTRATOASEO, 
 USUARIO.ESTADO, 
 USUARIO.APARTAMENTO, 
 USUARIO.INDDESHABITADO, 
 USOS.NOMBRE, 
 USUARIO.ASEO,
 USUARIO.ANO,
 USUARIO.PERIODO,
 USUARIO.TOTFACTURAPERACTUAL,
