SELECT USUARIO.CODIGORUTA "CODIGO RUTA",
  USUARIO.CODIGOINTERNO "CODIGO INTERNO",
  NVL(USUARIO.PRIMERAPELLIDO,' ') ||' '|| NVL(USUARIO.SEGUNDOAPELLIDO,' ')||' '|| NVL(USUARIO.NOMBRES,' ') "NOMBRE USUARIO",
  USUARIO.DIRTECNICA "DIRECCION TECNICA",
  BARRIOS.NOMBRE BARRIO,
  USUARIO.USO,
  USUARIO.ESTRATO,
  TO_CHAR(USUARIO.LECTURA1)  "LECT_ANT",
  TO_CHAR(USUARIO.LECTURA)   "LECT ACT",
  TO_CHAR(USUARIO.LECTURAAFORO) "LECTURA AF",
  TO_CHAR(USUARIO.CONSUMO) CONS,
  TO_CHAR(USUARIO.CONSUMOPROM) "CONS PROM",
  TO_CHAR(USUARIO.CARGOFIJO) "CARGO FIJO",
  VALCONSUMO+VALCONSUMOPROM+ACSINMEDICION "VALOR CONS",
  USUARIO.TOTALACUEDUCTO "TOTAL ACUEDUCT",
  USUARIO.TOTALALCANTARILLADO "TOTAL ALCANT",
  USUARIO.TOTALASEO  "TOTAL ASEO",
  TOTFACTURAPERACTUAL-TOTALDEUDA-TOTALASEO-TOTALALCANTARILLADO-TOTALACUEDUCTO OTROSCARGOS,
  USUARIO.TOTALDEUDA  "DEUDA ANTERIOR",
  USUARIO.PERIODOSATRASO  "PER ATR",
  USUARIO.TOTFACTURAPERACTUAL  "TOTAL FACTURADO",
  USUARIO.ESTADO,
  TO_CHAR(USUARIO.FECHAPAGOPERPROCESO,'DD/MM/YYYY') "FECHA PAGO",
  USUARIO.BANCOPERPROCESO  BANCO,
  USUARIO.PAQUETEPAGOPERPROCESO  "PAQ PAGO",
  USUARIO.NOFECHAPAGOPERPROCESO  "NO PAGO"
FROM    SP_USUARIO USUARIO 
LEFT JOIN BARRIOS
ON USUARIO.BARRIO=BARRIOS.CODIGO
WHERE USUARIO.ESTADO NOT IN ('R','S')
AND USUARIO.COMPANIA = s$compania$s  
s$condicion$s
AND USUARIO.CODIGORUTA BETWEEN s$codigoInicial$s AND s$codigoFinal$s
AND USO || ESTRATO BETWEEN s$usoIni$s || s$estratoIni$s AND s$usoFin$s  || s$estratoFin$s                   
AND USUARIO.BARRIO BETWEEN s$barrioIni$s AND s$barrioFin$s
s$condicionOrdenCodRuta$s 
