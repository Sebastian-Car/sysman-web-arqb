SELECT 
  SP_USUARIO.CICLO,
  SP_USUARIO.CODIGORUTA,
  SP_USUARIO.PRIMERAPELLIDO, 
  SP_USUARIO.SEGUNDOAPELLIDO,
  SP_USUARIO.NOMBRES, 
  SP_USUARIO.DIRTECNICA, 
  CASE SP_USUARIO.ESTADO  WHEN 'A' THEN 'Activo'
                          WHEN 'C' THEN 'CORTADO'
                          WHEN 'R' THEN 'RETIRADO'
                          WHEN 'S' THEN 'SUSPENDIDO'
  END ESTADO,
  SP_USUARIO.USO, 
  SP_USUARIO.ESTRATO, 
  SP_USUARIO.LECTURA1, 
  SP_USUARIO.LECTURA, 
  SP_USUARIO.CONSUMO, 
  SP_USUARIO.FECHACAMBIOEST, 
  SP_USUARIO.FECHARENTRADANUEVOUSUARIO
FROM SP_USUARIO 
WHERE SP_USUARIO.COMPANIA= s$compania$s
  AND (SP_USUARIO.FECHACAMBIOEST BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY') OR SP_USUARIO.FECHACAMBIOEST IS NULL) 
  AND SP_USUARIO.CICLO=s$ciclo$s
  AND SP_USUARIO.ESTADO='S'
ORDER BY SP_USUARIO.FECHACAMBIOEST
