SELECT 
  SP_USUARIO.CODIGORUTA,
  SP_USUARIO.ANO,
  SP_USUARIO.ESTADO,
  SP_USUARIO.PERIODO,
  SP_USUARIO.USO,
  SP_USUARIO.PRIMERAPELLIDO||' ' ||SP_USUARIO.SEGUNDOAPELLIDO||' '||SP_USUARIO.NOMBRES AS USUARIONOMBRES,
  SP_USUARIO.CODIGOINTERNO,
  SP_USUARIO.DIRTECNICA,
  SP_USUARIO.DIRGUIA,
  SP_USUARIO.DIRCORRESPONDENCIA,
  SP_USUARIO.MEDIDOR,
  SP_USUARIO.LECTURA1,
  SP_USUARIO.LECTURA,
  SP_USUARIO.LECTURA2,
  SP_USUARIO.PERIODOSACUMSINLECTURA,
  SP_USUARIO.LECTURAAFORO,
  SP_USUARIO.LECTURA-SP_USUARIO.LECTURA1 AS CNSAF,
  SP_USUARIO.CONSUMO,
  SP_USUARIO.CONSUMOPROM AS CAFORO,
 CASE WHEN CONSUMOPROM<40 THEN
	CASE WHEN CONSUMO>((SP_USUARIO.CONSUMO1+SP_USUARIO.CONSUMO2+SP_USUARIO.CONSUMO3+SP_USUARIO.CONSUMO4+SP_USUARIO.CONSUMO5+SP_USUARIO.CONSUMO)/6)*(1.65) THEN 'Superior al Promedio' ELSE CASE WHEN CONSUMO<((SP_USUARIO.CONSUMO1+SP_USUARIO.CONSUMO2+SP_USUARIO.CONSUMO3+SP_USUARIO.CONSUMO4+SP_USUARIO.CONSUMO5+SP_USUARIO.CONSUMO)/6)*(0.35) THEN 'Inferior al Promedio' ELSE 'Normal' END END
ELSE
	CASE WHEN CONSUMOPROM>=40 THEN
		CASE WHEN CONSUMO>((SP_USUARIO.CONSUMO1+SP_USUARIO.CONSUMO2+SP_USUARIO.CONSUMO3+SP_USUARIO.CONSUMO4+SP_USUARIO.CONSUMO5+SP_USUARIO.CONSUMO)/6)*(1.35) THEN 'Superior al Promedio' ELSE CASE WHEN CONSUMO<((SP_USUARIO.CONSUMO1+SP_USUARIO.CONSUMO2+SP_USUARIO.CONSUMO3+SP_USUARIO.CONSUMO4+SP_USUARIO.CONSUMO5+SP_USUARIO.CONSUMO)/6)*(0.65) THEN 'Inferior al Promedio' ELSE 'Normal' END END
	ELSE
		'NORMAL'
	END
END AS OBSERVACIONES,
  SP_USUARIO_PROBLEMA.PROBLEMA,
  SP_PROBLEMA.NOMBRE,
  SP_USUARIO_PROBLEMA.PERIODO AS PERIODOUSUARIOPROBLEMA

FROM SP_USUARIO
INNER JOIN SP_USUARIO_PROBLEMA 

ON SP_USUARIO.COMPANIA = SP_USUARIO_PROBLEMA.COMPANIA
AND SP_USUARIO.CICLO = SP_USUARIO_PROBLEMA.CICLO
AND SP_USUARIO.CODIGORUTA = SP_USUARIO_PROBLEMA.CODIGORUTA 
AND SP_USUARIO.PERIODO = SP_USUARIO_PROBLEMA.PERIODO

INNER JOIN SP_PROBLEMA 
ON SP_USUARIO_PROBLEMA.COMPANIA = SP_PROBLEMA.COMPANIA 
AND SP_USUARIO_PROBLEMA.PROBLEMA = SP_PROBLEMA.CODIGO 
AND SP_USUARIO_PROBLEMA.CLASE = SP_PROBLEMA.CLASEPROBLEMA
AND SP_USUARIO_PROBLEMA.PERIODO=SP_USUARIO.periodo
AND SP_USUARIO_PROBLEMA.ANO=SP_USUARIO.ANO
WHERE SP_USUARIO.COMPANIA   =s$compania$s 
AND SP_USUARIO.CODIGORUTA BETWEEN s$codigoInicial$s  AND s$codigoFinal$s 
AND SP_USUARIO.CICLO=s$ciclo$s
AND SP_USUARIO.ESTADO ='A'
AND SP_USUARIO.CONSUMO>=0
AND (CASE WHEN CONSUMOPROM<40 THEN
	CASE WHEN CONSUMO>((SP_USUARIO.CONSUMO1+SP_USUARIO.CONSUMO2+SP_USUARIO.CONSUMO3+SP_USUARIO.CONSUMO4+SP_USUARIO.CONSUMO5+SP_USUARIO.CONSUMO)/6)*(1.65) THEN 'Superior al Promedio' ELSE CASE WHEN CONSUMO<((SP_USUARIO.CONSUMO1+SP_USUARIO.CONSUMO2+SP_USUARIO.CONSUMO3+SP_USUARIO.CONSUMO4+SP_USUARIO.CONSUMO5+SP_USUARIO.CONSUMO)/6)*(0.35) THEN 'Inferior al Promedio' ELSE 'Normal' END END
ELSE
	CASE WHEN CONSUMOPROM>=40 THEN
		CASE WHEN CONSUMO>((SP_USUARIO.CONSUMO1+SP_USUARIO.CONSUMO2+SP_USUARIO.CONSUMO3+SP_USUARIO.CONSUMO4+SP_USUARIO.CONSUMO5+SP_USUARIO.CONSUMO)/6)*(1.35) THEN 'Superior al Promedio' ELSE CASE WHEN CONSUMO<((SP_USUARIO.CONSUMO1+SP_USUARIO.CONSUMO2+SP_USUARIO.CONSUMO3+SP_USUARIO.CONSUMO4+SP_USUARIO.CONSUMO5+SP_USUARIO.CONSUMO)/6)*(0.65) THEN 'Inferior al Promedio' ELSE 'Normal' END END
	ELSE
		'NORMAL'
	END
END )='Normal'
ORDER BY SP_USUARIO.CODIGORUTA
