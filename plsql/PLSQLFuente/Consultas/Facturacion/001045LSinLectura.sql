SELECT 
  CICLO,
  PERIODOSINLECTURA,
  ACUMULADO,
  CODIGORUTA,
  NVL(PRIMERAPELLIDO,' ') PRIMERAPELLIDO,
  NVL(SEGUNDOAPELLIDO,' ') SEGUNDOAPELLIDO,
  FECHARENTRADANUEVOUSUARIO,
  LECTURA1,
  LECTURA,
  CONSUMO,
  LECTURA2,
  NVL(NOMBRES,' ') NOMBRES,
  DIRTECNICA,
  INDDESHABITADO,
  CODIGOINTERNO,
  CONSUMO1,
  CONSUMO2 
FROM SP_USUARIO 
WHERE PERIODOSINLECTURA > 0 
AND COMPANIA       = s$compania$s 
AND CICLO               = s$ciclo$s 
ORDER BY 
  CICLO,
  CODIGORUTA
