SELECT 
	USUARIO.CODIGORUTA, 
	USUARIO.CODIGOINTERNO, 
	USUARIO.PRIMERAPELLIDO || ' ' || USUARIO.SEGUNDOAPELLIDO || ' ' || USUARIO.NOMBRES AS NOMBRE, 
	USUARIO.CICLO, 
	NVL(CONJUNTA_ASEO.VALORASEO,0) AS VALORASEO, 
	NVL(CONJUNTA_CONVENIO.TOTAL,0) AS VALORCONVENIO, 
	CONJUNTA_CONVENIO.ID_EMPRESA
FROM SP_USUARIO USUARIO 
		LEFT JOIN (
					SELECT 
						  HISTORIA_EXTERNA.COMPANIA, 
						  HISTORIA_EXTERNA.CICLO, 
						  HISTORIA_EXTERNA.CODIGORUTA, 
						  HISTORIA_EXTERNA.VALORASEO
					 FROM SP_HISTORIA_EXTERNA HISTORIA_EXTERNA 
					WHERE COMPANIA  			  = s$compania$s 
					  AND s$condicionCiclo$s ANO  = s$anio$s
					  AND PERIODO 	 			  = 's$periodo$s' 
					  AND CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
					ORDER BY CODIGORUTA
					) CONJUNTA_ASEO
			   ON USUARIO.COMPANIA 		= CONJUNTA_ASEO.COMPANIA
			  AND USUARIO.CICLO 		= CONJUNTA_ASEO.CICLO
			  AND USUARIO.CODIGORUTA 	= CONJUNTA_ASEO.CODIGORUTA  
		LEFT JOIN (SELECT 
					      HISTORIA_CONVENIOS.COMPANIA, 
					      HISTORIA_CONVENIOS.CICLO, 
					      HISTORIA_CONVENIOS.CODIGORUTA, 
					      HISTORIA_CONVENIOS.TOTAL,
					      HISTORIA_CONVENIOS.ID_EMPRESA 
					FROM  SP_HISTORIA_CONVENIOS HISTORIA_CONVENIOS 
					WHERE COMPANIA 				 = s$compania$s
					  AND s$condicionCiclo$s ANO = s$anio$s
					  AND PERIODO 			 	 ='s$periodo$s' 
					  AND NOCOBRAR 				 = 0 
					  AND CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
					ORDER BY CODIGORUTA
				   ) CONJUNTA_CONVENIO 
			   ON USUARIO.COMPANIA 		= CONJUNTA_CONVENIO.COMPANIA
			  AND USUARIO.CICLO 		= CONJUNTA_CONVENIO.CICLO
			  AND USUARIO.CODIGORUTA 	= CONJUNTA_CONVENIO.CODIGORUTA 
WHERE CONJUNTA_ASEO.VALORASEO IS NOT NULL 
   OR CONJUNTA_CONVENIO.TOTAL IS NOT NULL
