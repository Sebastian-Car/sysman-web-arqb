SELECT COMPANIA,
    CICLO,
    CODIGORUTA,
    NVL(PRIMERAPELLIDO, ' ') PRIMERAPELLIDO,
    NVL(SEGUNDOAPELLIDO, ' ') SEGUNDOAPELLIDO,
    NVL(NOMBRES, ' ') NOMBRES,
    DIRTECNICA,
    FECHASALIOUSUARIO
FROM SP_USUARIO
WHERE COMPANIA = s$compania$s
    s$condicionCiclo$s
    AND FECHASALIOUSUARIO BETWEEN s$fechaInicial$s AND s$fechaFinal$s
ORDER BY COMPANIA,
  CICLO,
  CODIGORUTA
