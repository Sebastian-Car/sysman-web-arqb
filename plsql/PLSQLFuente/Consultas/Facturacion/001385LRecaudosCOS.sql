SELECT SP_RECAUDOS.FECHA, 
      SP_RECAUDOS.BANCO, 
      SP_RECAUDOS.NUMEROPAQUETE, 
      SP_RECAUDOS.BARRAS, 
      SP_RECAUDOS.TIPO, 
      SP_RECAUDOS.VALORREPORTADO, 
      SP_RECAUDOS.CUPONESREPORTADOS, 
      SP_RECAUDOS.VALORREGISTRADO, 
      SP_RECAUDOS.CUPONESREGISTRADOS, 
      SP_RECAUDOS.DIFERENCIACUPONES, 
      SP_RECAUDOS.DIFERENCIAVALORES, 
      SP_RECAUDOS.USUARIO, 
      SP_BANCOS.NOMBRE,
      PCK_SYSMAN_UTL.FC_NOMBRE_MES(TO_NUMBER(TO_CHAR(SP_RECAUDOS.FECHA,'MM'))) MESNOMBRE,
      EXTRACT(YEAR FROM TO_DATE(TO_CHAR(SP_RECAUDOS.FECHA, 'DD/MM/YYYY'), 'DD/MM/YYYY')) AS ANO,
      EXTRACT(MONTH FROM TO_DATE(TO_CHAR(SP_RECAUDOS.FECHA, 'DD/MM/YYYY'), 'DD/MM/YYYY')) AS MES,
      EXTRACT(DAY FROM TO_DATE(TO_CHAR(SP_RECAUDOS.FECHA, 'DD/MM/YYYY'), 'DD/MM/YYYY')) AS DIA
FROM (SP_RECAUDOS 
LEFT JOIN SP_BANCOS 
      ON (SP_RECAUDOS.COMPANIA = SP_BANCOS.COMPANIA) 
      AND (SP_RECAUDOS.BANCO = SP_BANCOS.CODIGO)) 
LEFT JOIN COMPANIA 
  ON SP_RECAUDOS.COMPANIA = COMPANIA.CODIGO 
WHERE 
  SP_RECAUDOS.COMPANIA = 's$compania$s'
  AND SP_RECAUDOS.FECHA
  BETWEEN TO_DATE(s$fechaInicial$s, 'DD/MM/YYYY')  AND TO_DATE(s$fechaFinal$s, 'DD/MM/YYYY')
ORDER BY SP_RECAUDOS.COMPANIA
