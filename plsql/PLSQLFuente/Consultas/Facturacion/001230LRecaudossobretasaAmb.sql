SELECT  SP_USUARIO.CODIGORUTA,
        SP_USUARIO.PRIMERAPELLIDO || '' || SP_USUARIO.SEGUNDOAPELLIDO || ''|| SP_USUARIO.NOMBRES NOMBREUSUARIO,
        SP_TARIFAS.VALORTASAAMBIENTALAC  * SP_USUARIO.CONSUMO TASAAMBIENTALAC,
        SP_TARIFAS.VALORTASAAMBIENTALALC * SP_USUARIO.CONSUMO TASAAMBIENTALALC,
        NVL(SP_TARIFAS.VALORTASAAMBIENTALAC  * SP_USUARIO.CONSUMO,0) +
        NVL(SP_TARIFAS.VALORTASAAMBIENTALALC * SP_USUARIO.CONSUMO,0) TOTAL,
        SP_TARIFAS.USO,
        SP_USOS.NOMBRE NOMBREUSO,
        SP_TARIFAS.ESTRATO,
        SP_ESTRATOS.NOMBRE NOMBREESTRATO
FROM SP_USUARIO
  INNER JOIN SP_TARIFAS
    ON SP_USUARIO.COMPANIA   = SP_TARIFAS.COMPANIA
    AND SP_USUARIO.ANO       = SP_TARIFAS.ANO
    AND SP_USUARIO.PERIODO   = SP_TARIFAS.PERIODO
    AND SP_USUARIO.USO       = SP_TARIFAS.USO
    AND SP_USUARIO.ESTRATO   = SP_TARIFAS.ESTRATO
  INNER JOIN SP_USOS
    ON SP_TARIFAS.COMPANIA = SP_USOS.COMPANIA
    AND SP_TARIFAS.USO = SP_USOS.CODIGO
  INNER JOIN SP_ESTRATOS
    ON SP_TARIFAS.COMPANIA = SP_ESTRATOS.COMPANIA
    AND SP_TARIFAS.USO = SP_ESTRATOS.USO 
    AND SP_TARIFAS.ESTRATO = SP_ESTRATOS.CODIGO
WHERE SP_USUARIO.COMPANIA    = s$compania$s
  s$condicionCiclo$s
  AND SP_USUARIO.ESTADO NOT IN ('R','S')
  AND SP_USUARIO.BANCOPERPROCESO IS NOT NULL
  AND SP_USUARIO.FECHAPAGOPERPROCESO BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND SP_USUARIO.BANCOPERPROCESO BETWEEN s$bancoInicial$s AND s$bancoFinal$s
