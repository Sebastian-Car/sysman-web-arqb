SELECT DISTINCT SP_FACTURADO.COMPANIA,
        SP_FACTURADO.CICLO,
        SP_FACTURADO.CODIGORUTA,
        SP_FACTURADO.ANO,
        SP_USUARIO.BANCOPERPROCESO,
        SP_FACTURADO.PERIODO,
        SP_FACTURADO.CONCEPTO,
        PRIMERAPELLIDO || ' ' || SEGUNDOAPELLIDO || ' ' || NOMBRES AS NOMBRECOMPLETO,
        SP_FACTURADO.VALOR_FACTURADO,
        SP_FACTURADO.DEUDA,
        SP_FACTURADO.VALOR_FACTURADO+SP_FACTURADO.DEUDA AS TOTALFACTURADO,
        SP_USUARIO.ESTADO,
        SP_USUARIO.NOTACREDITO,
        SP_FACTURADO.SALDOCREDITO,
        SP_FACTURADO.CREDITOABONADO,
        SP_CONCEPTOS.NOMBRE AS NOMBRECONCEPTO,
        SP_CONCEPTOS.CODIGO,
        CASE NVL(SP_TBLHIST_SALDO_CREDITO.ESNORMAL, -1)
          WHEN  -1 THEN
            'Normal'
          ELSE
            'Pago Doble'
        END AS ESNORMAL,
        SP_TBLHIST_SALDO_CREDITO.BANCO
FROM SP_FACTURADO
  LEFT JOIN SP_USUARIO
         ON SP_FACTURADO.COMPANIA   = SP_USUARIO.COMPANIA
        AND SP_FACTURADO.CICLO      = SP_USUARIO.CICLO
        AND SP_FACTURADO.CODIGORUTA = SP_USUARIO.CODIGORUTA
        INNER JOIN SP_TBLHIST_SALDO_CREDITO
                ON SP_TBLHIST_SALDO_CREDITO.COMPANIA   = SP_FACTURADO.COMPANIA
               AND SP_TBLHIST_SALDO_CREDITO.CICLO      = SP_FACTURADO.CICLO
               AND SP_TBLHIST_SALDO_CREDITO.CODIGORUTA = SP_FACTURADO.CODIGORUTA
               AND SP_TBLHIST_SALDO_CREDITO.ANO        = SP_FACTURADO.ANO
               AND SP_TBLHIST_SALDO_CREDITO.PERIODO    = SP_FACTURADO.PERIODO
               LEFT JOIN SP_CONCEPTOS
                      ON SP_FACTURADO.CONCEPTO = SP_CONCEPTOS.CODIGO
                     AND SP_FACTURADO.COMPANIA = SP_CONCEPTOS.COMPANIA
WHERE SP_FACTURADO.COMPANIA = s$compania$s
  AND SP_FACTURADO.CODIGORUTA BETWEEN 's$codIni$s' AND 's$codFin$s'
  AND SP_FACTURADO.CONCEPTO NOT IN (17)
  AND (SP_FACTURADO.CONCEPTO BETWEEN 0 AND 49
       OR SP_FACTURADO.CONCEPTO IN (201,202,203,204,205,206,207,246,247,248,249))
  AND SP_USUARIO.ESTADO NOT IN ('R','S')
  AND SP_FACTURADO.SALDOCREDITO   <> 0
  AND SP_FACTURADO.CREDITOABONADO <> 0
  AND SP_USUARIO.BANCOPERPROCESO IS NOT NULL
  s$condicionCiclo$s
  s$condicionBanco$s
