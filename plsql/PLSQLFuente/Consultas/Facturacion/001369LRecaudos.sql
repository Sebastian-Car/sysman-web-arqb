SELECT 
	FECHA, 
	BANCO,
	BANCO_NOM,
	NUMEROPAQUETE,
	CUPONESREPORTADOS,
	VALORREPORTADO,
	CUPONESREGISTRADOS,
	VALORREGISTRADO,
	DIFERENCIACUPONES,
	DIFERENCIAVALORES,
	ANO,
	MESNOMBRE,
	MES,
	COMENTARIOS,
	COMPANIA,
	CONVENIOS,
	DIA,
	TOTAL,
	TERCERIZADO
FROM V_SP_RECAUDOS
WHERE 
  V_SP_RECAUDOS.COMPANIA = 's$compania$s' 
  AND V_SP_RECAUDOS.FECHA 
    BETWEEN s$fechaInicial$s
    AND s$fechaFinal$s
    s$condicion$s
    s$ordenyagrupado$s
