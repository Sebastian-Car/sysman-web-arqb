SELECT SP_USUARIO_ABONO.COMPANIA,
  SP_USUARIO.CICLO,
  SP_USUARIO.CODIGORUTA,
  NVL(SP_USUARIO.NOMBRES,'')
  ||' '
  ||NVL(SP_USUARIO.PRIMERAPELLIDO,'')
  ||' '
  ||NVL(SP_USUARIO.SEGUNDOAPELLIDO,'') NOMBRES,
  SP_USUARIO_ABONO.ANO,
  PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(SP_USUARIO_ABONO.COMPANIA,SP_USUARIO_ABONO.ANO,SP_USUARIO_ABONO.PERIODO, NULL) PERIODO,
  SP_USUARIO_ABONO.BANCO,
  SP_BANCOS.NOMBRE,
  SP_USUARIO_ABONO.PAQUETE,
  TO_CHAR(SP_USUARIO_ABONO.FECHA_PAGO,'DD/MM/YYYY') FECHA_PAGO,
  SP_USUARIO_ABONO.VALOR_PAGADO,
  TO_CHAR(SP_USUARIO_ABONO.FECHA_LIMITE,'DD/MM/YYYY') FECHA_LIMITE
FROM (SP_USUARIO_ABONO
LEFT JOIN SP_BANCOS
ON (SP_USUARIO_ABONO.COMPANIA = SP_BANCOS.COMPANIA)
AND (SP_USUARIO_ABONO.BANCO   = SP_BANCOS.CODIGO))
INNER JOIN SP_USUARIO
ON (SP_USUARIO_ABONO.COMPANIA    = SP_USUARIO.COMPANIA)
AND (SP_USUARIO_ABONO.CICLO      = SP_USUARIO.CICLO)
AND (SP_USUARIO_ABONO.CODIGORUTA = SP_USUARIO.CODIGORUTA)
WHERE SP_USUARIO_ABONO.COMPANIA  = s$compania$s
AND SP_USUARIO_ABONO.FECHA_PAGO BETWEEN s$fechaInicial$s AND s$fechaFinal$s
