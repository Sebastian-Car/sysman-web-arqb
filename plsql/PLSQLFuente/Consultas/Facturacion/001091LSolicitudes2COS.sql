SELECT CLASESOLICITUD, 
	NUMERO, 
	SS.NOMBRE, 
	DIRTECNICA,
 	DIRGUIA, 
 	TELEFONO, 
 	TO_CHAR(FECHASOLICITUD, 'DD/MM/YYYY') FECHASOLICITUD, 
 	SPS.NOMBRE AS TIPODEMATRICULA, 
 	NIT, 
 	USUARIO, 
 	(CASE WHEN 
 		ACTIVADO IN(0) 
 			THEN ' '
 		ELSE 'X'
 		END) ACTIVADO, 
 	NVL(PRIMERAPELLIDO,'') ||' '|| NVL(SEGUNDOAPELLIDO,'') ||' '|| NVL(NOMBRES,'') AS NOMBRECOMPLETO
FROM SP_SOLICITUDPORSERVICIO SPS 
	INNER JOIN SP_SOLICITUDSERVICIO SS 
		ON SPS.COMPANIA = SS.COMPANIA
 		AND CODIGO 		= CLASESOLICITUD
WHERE SS.COMPANIA= s$compania$s
 	AND FECHASOLICITUD BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  	AND CLASESOLICITUD = '0'||s$mostrar$s
ORDER BY SS.COMPANIA, 
	CLASESOLICITUD, 
	NUMERO
