SELECT F.CODIGORUTA, 
	F.CICLO, 
	F.ANOINICIAL, 
	F.PERIODOINICIAL, 
	F.CONCEPTO, 
	SUM(F.MONTOFINANCIAR) AS SUMADEMONTOFINANCIAR, 
	SUM(F.SALDOFINANCIABLE) AS SUMADESALDOFINANCIABLE, 
	SUM(F.INTERES) AS SUMADEINTERES, 
	F.ANO*100+F.PERIODO AS PERANO,
	C.NOMBRE AS NOMBRECONCEPTO, 
	CO.NOMBRE, 
	C.ACUMFIN, 
	NVL(U.PRIMERAPELLIDO,' ')||' '||NVL(U.SEGUNDOAPELLIDO,' ')||' '||NVL(U.NOMBRES,' ') AS NOMBRECOMPLETO
FROM SP_USUARIO U
	INNER JOIN SP_FINANCIABLES F
		ON U.COMPANIA 	= F.COMPANIA
		AND U.CICLO 	= F.CICLO 
		AND U.CODIGORUTA= F.CODIGORUTA
	INNER JOIN SP_CONCEPTOS C
		ON C.COMPANIA = F.COMPANIA
		AND C.CODIGO  = F.CONCEPTO
	INNER JOIN COMPANIA CO
		ON C.COMPANIA = CO.CODIGO 
WHERE F.COMPANIA=s$compania$s
	AND F.CICLO =s$ciclo$s
	AND F.ANOINICIAL*100+F.PERIODOINICIAL BETWEEN s$anioInicial$s*100+s$periodoInicial$s AND s$anioFinal$s*100+s$periodoFinal$s
	s$acumfin$s
GROUP BY F.COMPANIA, 
	F.CODIGORUTA, 
	F.CICLO, 
	F.ANOINICIAL, 
	F.PERIODOINICIAL, 
	F.CONCEPTO, 
	F.ANO*100+F.PERIODO, 
	C.NOMBRE, 
	CO.NOMBRE, 
	C.ACUMFIN, 
	NVL(PRIMERAPELLIDO,' ')||' '||NVL(SEGUNDOAPELLIDO,' ')||' '||NVL(NOMBRES,' ') 
ORDER BY F.PERIODOINICIAL, 
	F.CONCEPTO
