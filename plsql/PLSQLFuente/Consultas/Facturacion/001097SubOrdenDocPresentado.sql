SELECT  
  SP_ORDENDOCPRESENTADO.CLASEDOC,
  SP_ORDENDOCPRESENTADO.ORDENTRABAJO,
  SP_ORDENDOCPRESENTADO.DOCUMENTO,
  SP_DOCUMENTOS_MATRICULA.NOMBRE
FROM SP_ORDENDOCPRESENTADO 
  LEFT JOIN SP_DOCUMENTOS_MATRICULA 
     ON SP_ORDENDOCPRESENTADO.COMPANIA = SP_DOCUMENTOS_MATRICULA.COMPANIA
    AND SP_ORDENDOCPRESENTADO.DOCUMENTO = SP_DOCUMENTOS_MATRICULA.CODIGO
WHERE SP_ORDENDOCPRESENTADO.COMPANIA = s$compania$s
  AND SP_ORDENDOCPRESENTADO.CLASEDOC = '$P!{PR_CLASEDOC}'
  AND SP_ORDENDOCPRESENTADO.ORDENTRABAJO = $P!{PR_ORDENTRABAJO} 
ORDER BY SP_ORDENDOCPRESENTADO.DOCUMENTO
