SELECT SP_COBROSPERSUASIVOS.COMPANIA,
   (SP_COBROSPERSUASIVOS.IDCOBRO) AS ACTA_PERSUASIVO,
   SP_COBROSPERSUASIVOS.CICLO,
   SP_COBROSPERSUASIVOS.CODIGORUTA,
   SP_COBROSPERSUASIVOS.ANO,
   SP_COBROSPERSUASIVOS.PERIODO,
   CASE SP_COBROSPERSUASIVOS.ESTADO WHEN 'A' THEN 'Activo' WHEN 'C' THEN 'Cerrado' WHEN 'T' THEN 'Coactivo' ELSE '' END ESTADO,
   SP_COBROSPERSUASIVOS.PERIODOCIERRE,
  
  CASE
    WHEN SP_COBROSPERSUASIVOS.ANO IS NULL
    THEN ''
    ELSE PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(SP_COBROSPERSUASIVOS.COMPANIA,SP_COBROSPERSUASIVOS.ANO,SP_COBROSPERSUASIVOS.PERIODO,NULL)
  END PER,
   SP_COBROSPERSUASIVOS.NOTIFICADO,
   SP_COBROSPERSUASIVOS.NIT_NOTIFICADO,
   SP_COBROSPERSUASIVOS.NOMBRE_NOTIFICADO,
   SP_COBROSPERSUASIVOS.PERIODOSATRASO,
   SP_COBROSPERSUASIVOS.ANOCIERRE,
   SP_COBROSPERSUASIVOS.OBSERVACION,
   TO_CHAR(SP_COBROSPERSUASIVOS.FECHAACTA,'DD/MM/YYYY') FECHAACTA,
   TO_CHAR(SP_COBROSPERSUASIVOS.FECHACIERRE,'DD/MM/YYYY') FECHACIERRE,
   (SP_COBROSPERSUASIVOS.IDCOBRO_COACT) AS ACTA_COACTIVO
FROM SP_COBROSPERSUASIVOS
INNER JOIN SP_USUARIO 
ON SP_COBROSPERSUASIVOS.CODIGORUTA = SP_USUARIO.CODIGORUTA
AND SP_COBROSPERSUASIVOS.CICLO     = SP_USUARIO.CICLO
AND SP_COBROSPERSUASIVOS.COMPANIA  = SP_USUARIO.COMPANIA
WHERE SP_COBROSPERSUASIVOS.COMPANIA= s$compania$s
AND SP_COBROSPERSUASIVOS.CICLO     = s$ciclo$s
s$condicion$s
