SELECT 
  GERMAN12.CICLO,  
  GERMAN12.CODIGORUTA,  
  SUM (SP_FACTURADO.VALORFINACT) SUMADEVALORFINACT,  
  SUM (SP_FACTURADO.VALORFINANT) SUMADEVALORFINANT,
  GERMAN12.VALOR_FACTURADO, GERMAN12.DEUDA,  
  SUM (VALORFINANT + VALORFINACT) VALORFN,
  GERMAN12.ANO,  
  SUM (VALORFINANT + VALORFINACT) - (GERMAN12.VALOR_FACTURADO + GERMAN12.DEUDA) DIFERENCIA,
  GERMAN12.PERIODO,
  SP_FACTURADO.COMPANIA
FROM (  SELECT 
          SP_FACTURADO.COMPANIA,
          SP_FACTURADO.CICLO, 
          SP_FACTURADO.CODIGORUTA, 
          SP_FACTURADO.ANO, 
          SP_FACTURADO.PERIODO, 
          SP_FACTURADO.CONCEPTO, 
          SP_FACTURADO.VALOR_FACTURADO, 
          SP_FACTURADO.DEUDA
        FROM SP_CICLO
        INNER JOIN SP_FACTURADO 
           ON SP_CICLO.COMPANIA = SP_FACTURADO.COMPANIA 
          AND SP_CICLO.NUMERO = SP_FACTURADO.CICLO 
          AND SP_CICLO.ANO = SP_FACTURADO.ANO 
        AND SP_CICLO.PERIODO = SP_FACTURADO.PERIODO
        WHERE SP_CICLO.COMPANIA= s$compania$s
          AND SP_FACTURADO.VALOR_FACTURADO + SP_FACTURADO.DEUDA <> 0
          AND SP_FACTURADO.CONCEPTO=12
        GROUP BY SP_FACTURADO.COMPANIA, SP_FACTURADO.CICLO, SP_FACTURADO.CODIGORUTA, SP_FACTURADO.ANO, SP_FACTURADO.PERIODO, SP_FACTURADO.CONCEPTO, SP_FACTURADO.VALOR_FACTURADO, SP_FACTURADO.DEUDA
     ) GERMAN12 
  INNER JOIN SP_FACTURADO 
     ON GERMAN12.PERIODO = SP_FACTURADO.PERIODO 
    AND GERMAN12.ANO = SP_FACTURADO.ANO 
    AND GERMAN12.CODIGORUTA = SP_FACTURADO.CODIGORUTA
    AND GERMAN12.CICLO = SP_FACTURADO.CICLO 
    AND GERMAN12.COMPANIA = SP_FACTURADO.COMPANIA
WHERE GERMAN12.COMPANIA=s$compania$s
  AND (SP_FACTURADO.CONCEPTO BETWEEN 1 AND 48 OR SP_FACTURADO.CONCEPTO IN (246,247,248,249)) 
  AND SP_FACTURADO.CONCEPTO NOT IN (12,17)	 
GROUP BY GERMAN12.CICLO, GERMAN12.CODIGORUTA, GERMAN12.VALOR_FACTURADO, GERMAN12.DEUDA, GERMAN12.ANO, GERMAN12.PERIODO, SP_FACTURADO.COMPANIA
HAVING (SUM(VALORFINANT + VALORFINACT)) <> (GERMAN12.VALOR_FACTURADO + GERMAN12.DEUDA)	   
ORDER BY SUM(VALORFINANT + VALORFINACT)-(GERMAN12.VALOR_FACTURADO + GERMAN12.DEUDA)
