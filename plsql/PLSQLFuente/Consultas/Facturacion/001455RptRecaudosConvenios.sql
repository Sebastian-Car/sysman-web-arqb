SELECT SP_USUARIO.CODIGOINTERNO,
  SP_USUARIO.FACTURA,
  NVL(SP_HISTORIA_CONVENIOS.TOTAL,0) AS TERCERIZA,
  TO_CHAR(SP_HISTORIA_CONVENIOS.FECHA_PAGO,'DD/MM/YYYY') AS FECHA_PAGO,
  SP_USUARIO.NIT,
  SP_HISTORIA_CONVENIOS.BANCO_PAGO
FROM SP_USUARIO
INNER JOIN (
SELECT SP_HISTORIA_CONVENIOS.COMPANIA,
  SP_HISTORIA_CONVENIOS.CICLO,
  SP_HISTORIA_CONVENIOS.CODIGORUTA,
  SP_HISTORIA_CONVENIOS.ANO,
  SP_HISTORIA_CONVENIOS.PERIODO,
  SP_HISTORIA_CONVENIOS.TOTAL,
  SP_HISTORIA_CONVENIOS.FECHA_PAGO,
  SP_HISTORIA_CONVENIOS.BANCO_PAGO
FROM SP_HISTORIA_CONVENIOS
WHERE SP_HISTORIA_CONVENIOS.COMPANIA ='s$compania$s'
AND SP_HISTORIA_CONVENIOS.FECHA_PAGO IS NOT NULL
) SP_HISTORIA_CONVENIOS
ON SP_USUARIO.CODIGORUTA = SP_HISTORIA_CONVENIOS.CODIGORUTA
AND SP_USUARIO.CICLO     = SP_HISTORIA_CONVENIOS.CICLO
AND SP_USUARIO.COMPANIA  = SP_HISTORIA_CONVENIOS.COMPANIA
WHERE SP_HISTORIA_CONVENIOS.FECHA_PAGO BETWEEN s$fechaInicial$s AND s$fechaFinal$s
AND SP_USUARIO.COMPANIA='s$compania$s'
ORDER BY SP_USUARIO.COMPANIA
