SELECT  
        SP_USUARIO.CODIGORUTA,
        SP_USUARIO.CODIGOINTERNO,
        SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO || ' '|| SP_USUARIO.NOMBRES NOMBREUSUARIO,
        SP_USUARIO.NIT,
        SP_USUARIO.DIRTECNICA,
        SP_USUARIO.CODIGOCATASTRAL,
        DECODE(SP_USUARIO.NOTADEBITO,'U', 'Urbano', 'R', 'Rural', 'T', 'Todos') NOTADEBITO,
        DECODE(SP_USUARIO.ESTADO,'A','Activo','S','Suspendido', 'C','Cortado','R','Retirado')ESTADO,
        DECODE(SP_USUARIO.ACUEDUCTO,'-1', 'SI', '0', 'NO')ACUEDUCTO,
        DECODE(SP_USUARIO.ALCANTARILLADO,'-1','SI', '0','NO')ALCANTARILLADO,
        DECODE(SP_USUARIO.ASEO,'-1','SI','0','NO')ASEO,
        DECODE(SP_USUARIO.ALUMBRADO,'-1','SI','0','NO')ALUMBRADO,
        SP_USOS.NOMBRE NOMBREUSO, 
        SP_ESTRATOS.NOMBRE NOMBREESTRATO, 
       s$nomEstrato$s
        SP_USUARIO.MATRICULA,
        SP_USUARIO.FECHARENTRADANUEVOUSUARIO,
        SP_USUARIO.ANOENTRADANUEVOUSUARIO, 
        SP_USUARIO.PERENTRADANUEVOUSUARIO
FROM SP_USUARIO 
  INNER JOIN SP_ESTRATOS 
    ON SP_USUARIO.COMPANIA = SP_ESTRATOS.COMPANIA 
    AND SP_USUARIO.USO = SP_ESTRATOS.USO 
    AND SP_USUARIO.ESTRATO = SP_ESTRATOS.CODIGO 
  INNER JOIN SP_USOS 
    ON SP_USUARIO.COMPANIA = SP_USOS.COMPANIA 
    AND SP_USUARIO.USO = SP_USOS.CODIGO
 s$conEstrato$s
WHERE SP_USUARIO.COMPANIA = s$compania$s 
	AND SP_USUARIO.USO BETWEEN s$usoInicial$s AND s$usoFinal$s
	AND SP_USUARIO.ESTRATO BETWEEN s$estratoInicial$s AND s$estratoFinal$s 
  s$ciclo$s
	AND SP_USUARIO.ESTADO = s$estado$s
  s$ubicacion$s
ORDER BY  SP_USUARIO.CICLO, 
          SP_USUARIO.CODIGORUTA
