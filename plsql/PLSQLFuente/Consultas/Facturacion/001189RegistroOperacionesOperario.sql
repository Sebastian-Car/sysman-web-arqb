SELECT ITO.CODIGORUTA,
	ITO.CONSECUTIVO,
	TO_CHAR(ITO.FECHA, 'DD/MM/YYYY') FECHA,
	CASE ITO.TIPO_OPERACION 
    	WHEN '001' THEN 'SUSPENSION' 
    	WHEN '002' THEN 'CORTE' 
    	WHEN '003' THEN 'RECONEXION'
    	WHEN '004' THEN 'REINSTALACION'
  	END TIPO_OPERACION,
	TRIM(T.NOMBRE) AFORADOR,
	ITO.DESCRIPCION,
	ITO.LECTURA,
	M.CONSECUTIVO MEDIDOR,
	ITO.SELLO_RETIRADO,
	ITO.SELLO_INSTALADO,
	CASE ITO.ESTADO
    	WHEN 'A' THEN 'Activo'
    	WHEN 'S' THEN 'Suspendido'
    	WHEN 'C' THEN 'Cortado'
    	WHEN 'R' THEN 'Retirado'
    END ESTADO,
	TO_CHAR(ITO.HORAEJECUCION, 'HH24:MI:SS') HORAEJECUCION
FROM SP_USUARIO U 
	INNER JOIN SP_INFO_TIPO_OPERACION ITO
		ON U.COMPANIA 	 = ITO.COMPANIA
		AND U.CICLO 	 = ITO.CICLO
		AND U.CODIGORUTA = ITO.CODIGORUTA
	INNER JOIN SP_AFORADORES A
    	ON ITO.COMPANIA  = A.COMPANIA
    	AND ITO.AFORADOR = A.CODIGO
   	INNER JOIN TERCERO T
   		ON A.COMPANIA  = T.COMPANIA
   		AND A.NIT 	   = T.NIT
   		AND A.SUCURSAL = T.SUCURSAL	
    INNER JOIN SP_MEDIDOR M
    	ON ITO.COMPANIA 	= M.COMPANIA
    	AND ITO.CONSECUTIVO = M.CONSECUTIVO
WHERE U.COMPANIA=s$compania$s
	s$condicionCiclo$s 
	AND ITO.ANO * 100 + ITO.PERIODO BETWEEN s$peranoInicial$s AND s$peranoFinal$s
	s$informePor$s
	s$condicionFecha$s
	s$condicionHora$s
ORDER BY ITO.CICLO, 
	ITO.CODIGORUTA,
	ITO.CONSECUTIVO
