SELECT (PRIMERAPELLIDO || ' ' || SEGUNDOAPELLIDO || ' ' || NOMBRES ) AS NOMBRECOMPLETO, 
  TO_CHAR(SP_TBLHIST_SALDO_CREDITO.FECHA,'DD/MM/YYYY') AS STRFECHA, 
  SP_TBLHIST_SALDO_CREDITO.VALOR_SALDO, 
  SP_TBLHIST_SALDO_CREDITO.VALORDES, 
  SP_TBLHIST_SALDO_CREDITO.CODIGORUTA, 
  TO_CHAR(FECHA,'DD/MM/YYYY') AS FECHAN, 
  SP_TBLHIST_SALDO_CREDITO.BANCO, 
  SP_TBLHIST_SALDO_CREDITO.PERIODO, 
  SP_TBLHIST_SALDO_CREDITO.ANO, 
  SP_TBLHIST_SALDO_CREDITO.USUARIOREG, 
  SP_TBLHIST_SALDO_CREDITO.ESNORMAL, 
  CASE WHEN SP_TBLHIST_SALDO_CREDITO.ESNORMAL IN(-1) THEN 'Normal' ELSE  'Pago Doble' END AS TIPO_SALDO_CREDITO, 
  SP_BANCOS.NOMBRE 
FROM SP_TBLHIST_SALDO_CREDITO 
INNER JOIN SP_USUARIO 
  ON  SP_TBLHIST_SALDO_CREDITO.COMPANIA  = SP_USUARIO.COMPANIA 
  AND SP_TBLHIST_SALDO_CREDITO.CICLO     = SP_USUARIO.CICLO 
  AND SP_TBLHIST_SALDO_CREDITO.CODIGORUTA = SP_USUARIO.CODIGORUTA 
INNER JOIN SP_BANCOS 
  ON SP_TBLHIST_SALDO_CREDITO.COMPANIA = SP_BANCOS.COMPANIA 
  AND SP_TBLHIST_SALDO_CREDITO.BANCO = SP_BANCOS.CODIGO 
WHERE SP_TBLHIST_SALDO_CREDITO.COMPANIA ='s$compania$s'   
  s$concicionCiclo$s 
  AND  SP_TBLHIST_SALDO_CREDITO.BANCO 
  BETWEEN 's$bancoInicial$s' AND 's$bancoFinal$s' 
  AND SP_TBLHIST_SALDO_CREDITO.FECHA 
  BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY')  AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
