SELECT USUARIO.COMPANIA,
       USUARIO.CODIGORUTA,
       USUARIO.ANO,
       USUARIO.PERIODO,
       NOMBRES || ' ' || PRIMERAPELLIDO || ' ' || SEGUNDOAPELLIDO SUSCRIPTOR,
       USUARIO.CODIGOINTERNO,
       USUARIO.DIRTECNICA,
       USUARIO.DIRGUIA,
       USUARIO.USO,
       USUARIO.ESTRATO,
       USUARIO.PERIODOSATRASO,
       DECODE(USUARIO.ACUEDUCTO,-1,'SI',0,'NO') ACUEDUCTO,
       DECODE(USUARIO.ALCANTARILLADO,-1,'SI',0,'NO') ALCANTARILLADO,
       DECODE(USUARIO.ASEO,-1,'SI',0,'NO') ASEO,
       DECODE(USUARIO.ALUMBRADO,-1,'SI',0,'NO') ALUMBRADO,
       DECODE(USUARIO.INDDESHABITADO,-1,'SI',0,'NO') INDDESHABITADO,
       USUARIO.PERIODOSINLECTURA,
       USUARIO.CONSUMO6,
       USUARIO.CONSUMO5,
       USUARIO.CONSUMO4,
       USUARIO.CONSUMO3,
       USUARIO.CONSUMO2,
       USUARIO.CONSUMO1,
       USUARIO.CONSUMO,
       USUARIO.LECTURA6,
       USUARIO.LECTURA5,
       USUARIO.LECTURA4,
       USUARIO.LECTURA3,
       USUARIO.LECTURA2,
       USUARIO.LECTURA1,
       USUARIO.LECTURA,
       USUARIO.CONSUMOPROM,
       USUARIO.BANCOPERPROCESO,
       TO_CHAR(USUARIO.FECHAPAGOPERPROCESO,'DD/MM/YYYY') FECHAPAGOPERPROCESO,
       USUARIO.PAQUETEPAGOPERPROCESO,
       USUARIO.TOTFACTURAPERACTUAL,
       USUARIO.TIPOCOBRO,
       USUARIO.FACTURA,
       USUARIO.COMENTARIOS,
       FACTURADO.CONCEPTO,
       FACTURADO.VALOR_FACTURADO,
       FACTURADO.DEUDA,
       USUARIO.CICLO,
       NVL(VALOR_FACTURADO,0) + NVL(FACTURADO.DEUDA,0) AS TOT, 
       SP_CONCEPTOS.NOMBRE NOM_CONCEPTO,
       SP_BANCOS.NOMBRE NOM_BANCOPERPROCESO,
       PCK_SERVICIOS_PUBLICOS_COM2.FC_NOMBREPERIODOCORTO(USUARIO.COMPANIA,USUARIO.ANO,USUARIO.PERIODO, NULL) NOMBREPERIODOCORTO
FROM   SP_USUARIO USUARIO
INNER  JOIN SP_FACTURADO FACTURADO
   ON  USUARIO.COMPANIA   = FACTURADO.COMPANIA
  AND  USUARIO.CICLO      = FACTURADO.CICLO
  AND  USUARIO.CODIGORUTA = FACTURADO.CODIGORUTA
  AND  USUARIO.ANO        = FACTURADO.ANO
  AND  USUARIO.PERIODO    = FACTURADO.PERIODO
INNER  JOIN SP_CONCEPTOS 
   ON  FACTURADO.COMPANIA = SP_CONCEPTOS.COMPANIA
  AND  FACTURADO.CONCEPTO = SP_CONCEPTOS.CODIGO   
LEFT   JOIN SP_BANCOS 
   ON  USUARIO.COMPANIA        = SP_BANCOS.COMPANIA
  AND  USUARIO.BANCOPERPROCESO = SP_BANCOS.CODIGO 
WHERE  USUARIO.COMPANIA   = 's$compania$s'
  AND  USUARIO.CICLO      = 's$ciclo$s'
  AND  USUARIO.CODIGORUTA = 's$codigoruta$s'
  AND  (NVL(VALOR_FACTURADO,0) + NVL(FACTURADO.DEUDA,0))<>0
