SELECT TO_CHAR(SP_FRAUDES.CONSECUTIVO) AS "Consecutivo",
       SP_USUARIO.NOMBRES || ' ' || SP_USUARIO.PRIMERAPELLIDO || ' ' 
       || SP_USUARIO.SEGUNDOAPELLIDO AS "Suscriptor",
       SP_USUARIO.DIRTECNICA AS "Dirección",
       SP_USUARIO.CODIGOINTERNO AS "Código Interno",
       SP_USUARIO.PERIODOSATRASO AS "Periodos de Atraso",
       SP_USOS.NOMBRE AS "Uso",
       SP_ESTRATOS.NOMBRE AS "Estrato",
    NVL(SP_MEDIDOR.CODIGO,'') AS "Medidor",
       SP_USUARIO.LECTURA AS "Lectura",
       TO_CHAR(SP_FRAUDES.FECHA, 'DD/MM/YYYY') AS "Fecha",
       PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(
       	SP_FRAUDES.COMPANIA, SP_FRAUDES.ANO, SP_FRAUDES.PERIODO,NULL) AS "Periodo Proceso",
       TO_CHAR(SP_FRAUDES.FECHAINSPECCION, 'DD/MM/YYYY') AS "Inspección",
       TO_CHAR(SP_FRAUDES.FECHANOTIFICACION, 'DD/MM/YYYY') AS "Notificación",
       TO_CHAR(SP_FRAUDES.FECHAMEDIACION, 'DD/MM/YYYY') AS "Mediación",
       SP_FRAUDES_TIPO.NOMBRE AS "Tipo Fraude",
       SP_FRAUDES.VALORACOBRAR AS "Valor a Cobrar",
       SP_FRAUDES.CUOTASDECOBRO AS "Cuotas",
       SP_FRAUDES.ANOCOBRO AS "Año Cobro",
       SP_FRAUDES.PERIODOCOBRO AS "Periodo Cobro",
       TO_CHAR(SP_FRAUDES.FECHACIERRE, 'DD/MM/YYYY') AS "Cierre",
       CASE WHEN SP_FRAUDES.ANOCIERRE IS NULL OR SP_FRAUDES.PERIODOCIERRE IS NULL THEN  ''
       ELSE PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(
       	SP_FRAUDES.COMPANIA,SP_FRAUDES.ANOCIERRE,SP_FRAUDES.PERIODOCIERRE,NULL) 
       END AS "Periodo Cierre",
       CASE SP_FRAUDES.ESTADO 
       WHEN 'A' THEN 'Abierto'
       WHEN 'C' THEN 'Cerrado' 
       ELSE '' END AS "Estado",
       SP_FRAUDES.DECISION AS "Decisión"
  FROM SP_FRAUDES
       INNER JOIN SP_USUARIO ON (SP_FRAUDES.COMPANIA = SP_USUARIO.COMPANIA)
       AND (SP_FRAUDES.CICLO = SP_USUARIO.CICLO)
       AND (SP_FRAUDES.CODIGORUTA = SP_USUARIO.CODIGORUTA)
       LEFT JOIN SP_USOS ON (SP_USUARIO.COMPANIA = SP_USOS.COMPANIA)
       AND (SP_USUARIO.USO = SP_USOS.CODIGO)
       LEFT JOIN SP_ESTRATOS ON (SP_USUARIO.COMPANIA = SP_ESTRATOS.COMPANIA)
       AND (SP_USUARIO.USO = SP_ESTRATOS.USO)
       AND (SP_USUARIO.ESTRATO = SP_ESTRATOS.CODIGO)
       INNER JOIN SP_FRAUDES_TIPO ON (SP_FRAUDES.COMPANIA = SP_FRAUDES_TIPO.COMPANIA)
       AND (SP_FRAUDES.TIPO = SP_FRAUDES_TIPO.CONSECUTIVO)
INNER JOIN SP_MEDIDOR
       ON SP_USUARIO.COMPANIA = SP_MEDIDOR.COMPANIA
    AND SP_USUARIO.MEDIDOR= SP_MEDIDOR.CONSECUTIVO
 WHERE SP_FRAUDES.COMPANIA = s$compania$s
   AND TRUNC(SP_FRAUDES.FECHA) BETWEEN  s$fechaInicial$s AND s$fechaFinal$s

   AND SP_FRAUDES.CODIGORUTA BETWEEN 's$rutaInicial$s' AND 's$rutaFinal$s'
s$ciclo$s
   s$criterioEstado$s
