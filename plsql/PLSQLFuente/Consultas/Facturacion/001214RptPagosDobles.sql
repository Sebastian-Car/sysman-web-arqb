SELECT D.CONCEPTO,
	C.NOMBRE AS NOMBRECONCEPTO,
	SUM(D.VALOR) AS VLRCONCEPTO
FROM SP_D_PAGOSDOBLES D
	INNER JOIN SP_CONCEPTOS C
		ON D.COMPANIA = C.COMPANIA
		AND D.CONCEPTO= C.CODIGO
WHERE D.FECHA 	BETWEEN s$fechaInicial$s AND s$fechaFinal$s
	AND D.BANCO BETWEEN s$bancoInicial$s AND s$bancoFinal$s
GROUP BY D.COMPANIA,
	D.CONCEPTO,
	C.NOMBRE
HAVING D.COMPANIA=s$compania$s
	AND D.CONCEPTO BETWEEN 1 AND 49
	OR D.CONCEPTO IN (246,247,248,249)
