SELECT SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO || ' ' || SP_USUARIO.NOMBRES AS NOMBRECOMPLETO, 
SP_CONCEPTOS.CODIGO,
SP_CONCEPTOS.NOMBRE AS NOMBRECONCEPTO, 
SP_FACTURADO.VALOR_FACTURADO, 
SP_FACTURADO.DEUDA,
SP_FACTURADO.VALOR_FACTURADO+SP_FACTURADO.DEUDA AS TOTALFACTURADO, 
SP_USUARIO.COMPANIA, 
SP_USUARIO.CICLO, 
SP_USUARIO.CODIGORUTA, 
SP_USUARIO.PERIODOSATRASO, 
SP_FACTURADO.CONCEPTO,
SP_USUARIO.ANO, 
SP_USUARIO.PERIODO
FROM SP_FACTURADO INNER JOIN SP_USUARIO ON (SP_FACTURADO.CODIGORUTA = SP_USUARIO.CODIGORUTA) 
AND (SP_FACTURADO.CICLO = SP_USUARIO.CICLO) 
AND (SP_FACTURADO.COMPANIA = SP_USUARIO.COMPANIA) 
INNER JOIN COMPANIA ON (SP_FACTURADO.COMPANIA = COMPANIA.CODIGO) 
LEFT JOIN SP_CONCEPTOS ON (SP_FACTURADO.CONCEPTO = SP_CONCEPTOS.CODIGO) 
AND (SP_FACTURADO.COMPANIA = SP_CONCEPTOS.COMPANIA)
WHERE (SP_FACTURADO.VALOR_FACTURADO+SP_FACTURADO.DEUDA) > s$valores$s 
AND SP_USUARIO.COMPANIA =  s$compania$s
AND SP_USUARIO.PERIODOSATRASO >= s$atraso$s
AND SP_FACTURADO.CONCEPTO BETWEEN s$conceptoInicial$s AND s$conceptoFinal$s
 s$condicionCiclo$s
ORDER BY SP_USUARIO.COMPANIA, SP_USUARIO.CICLO, SP_USUARIO.CODIGORUTA
