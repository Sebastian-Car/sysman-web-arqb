SELECT   SP_D_RECAUDO.CONCEPTO
        ,SP_CONCEPTOS.NOMBRE NOMBRECONCEPTO
        ,SUM(SP_D_RECAUDO.VALORDEUDA) VALORDEUDA
        ,SP_SERVICIO.NOMBRE SERV
        ,SUM(SP_D_RECAUDO.VALORABONOANT) VALORABONOANT
FROM     SP_D_RECAUDO
  LEFT JOIN SP_CONCEPTOS
    ON      SP_D_RECAUDO.COMPANIA = SP_CONCEPTOS.COMPANIA
   AND      SP_D_RECAUDO.CONCEPTO = SP_CONCEPTOS.CODIGO
  INNER JOIN SP_SERVICIO
     ON      SP_CONCEPTOS.COMPANIA = SP_SERVICIO.COMPANIA
    AND      SP_CONCEPTOS.SERVICIO = SP_SERVICIO.CODIGO
WHERE    (SP_D_RECAUDO.CONCEPTO BETWEEN 1 AND 48
   OR    SP_D_RECAUDO.CONCEPTO  IN (201,202,203,204,205,206,207,246,247,248,249))
  AND    SP_D_RECAUDO.CONCEPTO  NOT IN (12,17)
  AND    SP_D_RECAUDO.COMPANIA  = s$compania$s
  AND    SP_D_RECAUDO.FECHA     IS NOT NULL
  AND    SP_D_RECAUDO.FECHA     BETWEEN s$fechaInicial$s AND s$fechaFinal$s
GROUP BY SP_D_RECAUDO.CONCEPTO
        ,SP_CONCEPTOS.NOMBRE
        ,SP_SERVICIO.NOMBRE 
