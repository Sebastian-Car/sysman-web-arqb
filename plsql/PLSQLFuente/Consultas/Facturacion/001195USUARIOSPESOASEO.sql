SELECT TMP_USUARIOS_PESOASEO.COMPANIA,
  TMP_USUARIOS_PESOASEO.CICLO,
  TMP_USUARIOS_PESOASEO.CODIGORUTA,
  TMP_USUARIOS_PESOASEO.USO,
  TMP_USUARIOS_PESOASEO.ESTRATO,
  TMP_USUARIOS_PESOASEO.ANO,
  TMP_USUARIOS_PESOASEO.PERIODO,
  TMP_USUARIOS_PESOASEO.PESOASEO,
  USUARIO.PRIMERAPELLIDO,
  usuario.SEGUNDOAPELLIDO, 
  USUARIO.NOMBRES, 
  USUARIO.ASEO, 
  USUARIO.CODIGOINTERNO 
FROM SP_TMP_USUARIOS_PESOASEO TMP_USUARIOS_PESOASEO 
  INNER JOIN SP_USUARIO USUARIO
    ON  TMP_USUARIOS_PESOASEO.COMPANIA   = USUARIO.COMPANIA
    AND TMP_USUARIOS_PESOASEO.CICLO      = USUARIO.CICLO 
    AND TMP_USUARIOS_PESOASEO.CODIGORUTA = USUARIO.CODIGORUTA 
WHERE TMP_USUARIOS_PESOASEO.COMPANIA = s$compania$s
AND TMP_USUARIOS_PESOASEO.PESOASEO   NOT IN (1,0)
AND USUARIO.ASEO NOT                 IN (0)
