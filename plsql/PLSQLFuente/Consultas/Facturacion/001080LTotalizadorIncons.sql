WITH PRIMERTOTALIZADOR AS(
    SELECT COMPANIA, 
        CICLO, 
        CODIGORUTA, 
        NVL(PRIMERAPELLIDO, ' ') || ' ' || NVL(SEGUNDOAPELLIDO, ' ') || ' ' || NVL(NOMBRES, ' ') AS NOMBRE,
        DIRTECNICA,
        DIRGUIA,
        CONSUMO, 
        TOTALIZADOR,
        (CASE WHEN TOTALIZADOR <> 'T' 
            THEN LAG(CODIGORUTA) OVER(ORDER BY COMPANIA, CICLO, CODIGORUTA DESC)
        END) ANTERIOR
    FROM SP_USUARIO
    WHERE COMPANIA = s$compania$s
        AND CICLO = s$ciclo$s
        AND TOTALIZADOR IS NOT NULL
    ORDER BY COMPANIA, CICLO, CODIGORUTA DESC),
DATOSAREACOMUN AS(SELECT COMPANIA COMPANIADET, 
        CICLO CICLODET, 
        CODIGORUTA CODIGORUTADET, 
        (CASE 
        WHEN TOTALIZADOR = 'S' 
        THEN(CASE WHEN LAG(ANTERIOR) OVER (ORDER BY COMPANIA, CICLO, CODIGORUTA DESC)IS NULL
            THEN ANTERIOR
            ELSE (SELECT MIN(CODIGORUTA)
                  FROM PRIMERTOTALIZADOR
                  WHERE PRIMERTOTALIZADOR.CODIGORUTA > SEGUNDO.CODIGORUTA
                      AND PRIMERTOTALIZADOR.TOTALIZADOR = 'T')
            END)
       ELSE NULL
      END) AS CODIGOTOTALIZADOR,
        CONSUMO CONSUMODET, 
        DIRTECNICA DIRTECNICADET, 
        NOMBRE NOMBREDET,
        DIRGUIA DIRGUIADET,
        TOTALIZADOR TOTALIZADORDET
FROM PRIMERTOTALIZADOR SEGUNDO)
SELECT COMPANIA,
	CICLO,
	CODIGORUTA,
	NVL(PRIMERAPELLIDO, ' ') || ' ' || NVL(SEGUNDOAPELLIDO, ' ') || ' ' || NVL(NOMBRES, ' ') AS NOMBRE,
	DIRTECNICA,
	DIRGUIA,
	CONSUMO,
	'3. SUMA DE USUARIOS DE AREA COMÚN MAYOR A CONSUMO DEL TOTALIZADOR AREA COMÚN' AS OPCION  
FROM SP_USUARIO INNER JOIN DATOSAREACOMUN DAC
	ON CODIGORUTA = CODIGOTOTALIZADOR
	AND COMPANIA  = COMPANIADET
	AND CICLO 	  = CICLODET
WHERE CODIGOTOTALIZADOR IS NOT NULL
  	AND COMPANIA = s$compania$s
  	AND CICLO = s$ciclo$s
GROUP BY COMPANIA,
	CICLO,
	CODIGORUTA,
  	NVL(PRIMERAPELLIDO, ' ') || ' ' || NVL(SEGUNDOAPELLIDO, ' ') || ' ' || NVL(NOMBRES, ' '),
	DIRTECNICA,
	DIRGUIA,
	CONSUMO
HAVING CONSUMO - SUM(CONSUMODET)<0
UNION
SELECT COMPANIA,
	CICLO,
	CODIGORUTA,
	NOMBREDET,
	DIRTECNICA,
	DIRGUIA,
	CONSUMO,
	'2. USUARIO DE AREA COMÚN SIN ASIGNAR A TOTALIZADOR AREA COMÚN' AS OPCION 
FROM SP_USUARIO LEFT JOIN DATOSAREACOMUN DAC
	ON CODIGORUTA = CODIGORUTADET
	AND COMPANIA  = COMPANIADET
	AND CICLO 	  = CICLODET
WHERE CODIGOTOTALIZADOR IS NULL
  	AND COMPANIA = s$compania$s
  	AND CICLO = s$ciclo$s
  	AND TOTALIZADOR IN ('S')
UNION
SELECT COMPANIADET,
	CICLODET,
	CODIGORUTADET,
	NOMBREDET,
	DIRTECNICADET,
	DIRGUIADET,
	CONSUMODET,
	'1. TOTALIZADOR AREA COMÚN SIN USUARIOS DE AREA COMÚN' AS OPCION  
FROM DATOSAREACOMUN DAC
  WHERE CODIGOTOTALIZADOR IS NOT NULL
  AND COMPANIADET IS NULL
