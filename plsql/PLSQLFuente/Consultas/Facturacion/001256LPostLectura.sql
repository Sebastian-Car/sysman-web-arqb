
SELECT 
    SP_PLANOSDIGITAL.CICLO, 
    PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(SP_PLANOSDIGITAL.COMPANIA, PCK_SERVICIOS_PUBLICOS_COM2.FC_ANOSIGUIENTE(SP_PLANOSDIGITAL.COMPANIA,SP_PLANOSDIGITAL.ANO, SP_PLANOSDIGITAL.PERIODO , ''), PCK_SERVICIOS_PUBLICOS_COM2.FC_PERIODOSIGUIENTE(SP_PLANOSDIGITAL.COMPANIA,SP_PLANOSDIGITAL.ANO, SP_PLANOSDIGITAL.PERIODO , ''), '') PERIODOTRABAJO,
    SP_PLANOSDIGITAL.FECHASUBIDA  , 
    SUBSTR(CODIGORUTA, 1, 2)  RUTA, 
    SP_USUARIO.CODIGORUTA, 
    SP_USUARIO.NOMBRES || ' ' || SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO  NOMBREUSUARIO, 
    SP_USUARIO.DIRTECNICA, 
    SP_USUARIO.LECTURA, 
    SP_USUARIO.LECTURAAFORO, 
    SP_PLANOSDIGITAL.MAECNS, 
    (SELECT NOMBRE FROM SP_PROBLEMA WHERE COMPANIA = SP_PLANOSDIGITAL.COMPANIA AND CLASEPROBLEMA = 'AFR' AND CODIGO = SP_PLANOSDIGITAL.MAEPRB) MAEPRB, 
    CASE WHEN 
        SP_PLANOSDIGITAL.COD_ERROR = 'V'
    THEN 
        'MEDIDOR DIO LA VUELTA'
    ELSE
        CASE WHEN 
           SP_PLANOSDIGITAL.COD_ERROR = 'I'
       THEN 
           'MEDIDOR INVERTIDO'
       ELSE
           CASE WHEN 
              SP_PLANOSDIGITAL.COD_ERROR = 'E'
          THEN 
              'ERROR EN LECTURA (CONSUMO ELEVADO)'
          ELSE
              CASE WHEN 
                 SP_PLANOSDIGITAL.COD_ERROR = 'T'
             THEN 
                 'MEDIDOR TRANCADO'
             ELSE
                 CASE WHEN 
                    SP_PLANOSDIGITAL.COD_ERROR = 'N'
                THEN 
                    'NORMAL'
                ELSE
                    'SIN DEFINIR'
                END
             END
          END
       END
    END  CODERROR, 
    SP_USUARIO.CODIGOINTERNO, 
    SP_USUARIO.MEDIDOR, 
    SP_USUARIO.ESTADO, 
    SP_AFORADORES.CODIGO  CODAFORADOR, 
    (SELECT NOMBRE FROM TERCERO WHERE COMPANIA = SP_AFORADORES.COMPANIA AND NIT = SP_AFORADORES.NIT AND SUCURSAL = SP_AFORADORES.SUCURSAL)  NOMBREAFORADOR, 
    s$fechaCampo$s  
FECHASELECCION
FROM SP_PLANOSDIGITAL 
  LEFT JOIN SP_USUARIO 
    ON  SP_PLANOSDIGITAL.COMPANIA = SP_USUARIO.COMPANIA
    AND SP_PLANOSDIGITAL.CICLO = SP_USUARIO.CICLO
    AND SP_PLANOSDIGITAL.MAECOD1 = SP_USUARIO.CODIGOINTERNO
  LEFT JOIN SP_AFORADORES 
    ON  SP_PLANOSDIGITAL.COMPANIA = SP_AFORADORES.COMPANIA
    AND SP_PLANOSDIGITAL.AFORADOR = SP_AFORADORES.CODIGO
WHERE  SP_PLANOSDIGITAL.COMPANIA = s$compania$s
AND   s$condicionFecha$s
AND    LPAD(SP_PLANOSDIGITAL.ANO, 4,'0') || LPAD(SP_PLANOSDIGITAL.PERIODO, 2, '0') BETWEEN LPAD(s$anoInicial$s, 4,'0') || LPAD(s$periodoInicial$s, 2, '0') AND LPAD(s$anoFinal$s, 4,'0') || LPAD(s$periodoFinal$s, 2, '0')
ORDER BY 1
