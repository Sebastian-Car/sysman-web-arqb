SELECT SP_HISTORICOFACTURA.COMPANIA, 
  SP_HISTORICOFACTURA.CODIGOINTERNO,  
  SP_HISTORICOFACTURA.NIT, 
  SP_HISTORICOFACTURA.NOMBRES, 
  SP_HISTORICOFACTURA.DIRTECNICA, 
  SUM(SP_HISTORICOFACTURA.TOTALACUEDUCTO)      AS TOTALACUEDUCTO, 
  SUM(SP_HISTORICOFACTURA.TOTALALCANTARILLADO) AS TOTALALCANTARILLADO, 
  SUM(SP_HISTORICOFACTURA.TOTALASEO)           AS TOTALASEO 
FROM SP_HISTORICOFACTURA 
INNER JOIN SP_PAGO 
  ON  SP_HISTORICOFACTURA.COMPANIA = SP_PAGO.COMPANIA 
  AND SP_HISTORICOFACTURA.PERIODO = SP_PAGO.PERIODO 
  AND SP_HISTORICOFACTURA.ANO = SP_PAGO.ANO 
  AND SP_HISTORICOFACTURA.CODIGOINTERNO = SP_PAGO.CODIGOINTERNO 
WHERE SP_HISTORICOFACTURA.COMPANIA ='s$compania$s' 
  s$condicionCiclo$s
  AND SP_HISTORICOFACTURA.ANO = s$ano$s 
  AND SP_HISTORICOFACTURA.USO ='04' 
  AND EXTRACT(MONTH FROM SP_PAGO.FECHA) s$trimestre$s 
GROUP BY SP_HISTORICOFACTURA.COMPANIA, 
  SP_HISTORICOFACTURA.CODIGOINTERNO, 
  SP_HISTORICOFACTURA.NIT,  
  SP_HISTORICOFACTURA.NOMBRES, 
  SP_HISTORICOFACTURA.DIRTECNICA
