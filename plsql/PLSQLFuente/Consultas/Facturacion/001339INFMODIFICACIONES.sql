SELECT 's$companiaNit$s' COMPANIANIT,
      TRUNC( SP_MODIFICACIONES.FECHA) FECHA, 
       SP_USUARIO.ESTRATO,
       SP_USUARIO.DIRTECNICA,
       SP_MODIFICACIONES.CODIGORUTA,
       SP_USUARIO.CODIGOINTERNO, 
       SP_USUARIO.NOMBRES || ' ' || SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO NOMBREUSUARIO, 
       SP_USUARIO.CICLO, 
       SP_MODIFICACIONES.OBSERVACIONES,
       SP_MODIFICACIONESDEUDA.CONCEPTO,
       SP_CONCEPTOS.NOMBRE,
       SP_MODIFICACIONESDEUDA.VRANT, 
       SP_MODIFICACIONESDEUDA.VRNUE,
       NVL(SP_MODIFICACIONESDEUDA.VRANT - SP_MODIFICACIONESDEUDA.VRNUE, 0) DIFERENCIA, 
       SP_USUARIO.NIT,
       's$companiaSigla$s' SIGLACOMPANIA,
       's$companiaNombre$s' CIA
 FROM SP_MODIFICACIONES 
INNER JOIN SP_USUARIO
   ON SP_MODIFICACIONES.COMPANIA   = SP_USUARIO.COMPANIA
  AND SP_MODIFICACIONES.CODIGORUTA = SP_USUARIO.CODIGORUTA
  AND SP_MODIFICACIONES.CICLO      = SP_USUARIO.CICLO
LEFT JOIN SP_MODIFICACIONESDEUDA 
   ON SP_MODIFICACIONES.COMPANIA   = SP_MODIFICACIONESDEUDA.COMPANIA
  AND SP_MODIFICACIONES.CODIGORUTA = SP_MODIFICACIONESDEUDA.CODIGORUTA 
  AND SP_MODIFICACIONES.CICLO      = SP_MODIFICACIONESDEUDA.CICLO 
  AND SP_MODIFICACIONES.FECHA      = SP_MODIFICACIONESDEUDA.FECHA
  AND SP_MODIFICACIONES.HORA       = SP_MODIFICACIONESDEUDA.HORA 
LEFT JOIN SP_CONCEPTOS 
   ON SP_MODIFICACIONESDEUDA.COMPANIA = SP_CONCEPTOS.COMPANIA
  AND SP_MODIFICACIONESDEUDA.CONCEPTO = SP_CONCEPTOS.CODIGO  
WHERE SP_MODIFICACIONES.COMPANIA =s$compania$s 
  AND SP_MODIFICACIONES.CODIGO   = 's$codigo$s'  
  AND SP_MODIFICACIONES.FECHA= s$fecha$s
  AND SP_MODIFICACIONES.HORA = s$hora$s
  AND SP_MODIFICACIONES.TIPO = s$tipo$s
