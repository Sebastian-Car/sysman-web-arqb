SELECT SP_USUARIO.COMPANIA,
  SP_USUARIO.CICLO,
  SP_USUARIO.CODIGORUTA,
  SP_USUARIO.CODIGOINTERNO,
  (Trim(primerapellido) || '' || Trim(segundoapellido)) AS Nombrecompleto,
  TO_CHAR(SP_USUARIO.FECHALECTURAAFORO,'DD/MM/YYYY') AS FECHALECTURAAFORO,
  TO_CHAR(SP_USUARIO.FECHALECANTERIOR,'DD/MM/YYYY') AS FECHALECANTERIOR,
  TO_CHAR(SP_USUARIO.FECHALECACTUAL,'DD/MM/YYYY') AS FECHALECACTUAL,
  DECODE(SP_USUARIO.INDMEDCAMBIADO,0,'','X') AS INDMEDCAMBIADO, 
  SP_USUARIO.ANOCAMBIOMEDIDOR,
  SP_USUARIO.PERIODOCAMBIOMEDIDOR,
  SP_USUARIO.CONSUMOMEDIDORANT,
  SP_USUARIO.MATRICULA
FROM SP_USUARIO
WHERE SP_USUARIO.COMPANIA= s$compania$s
  AND SP_USUARIO.CODIGORUTA
  BETWEEN 's$codigoInicialSeleccion$s' 
  AND 's$codigoFinalSeleccion$s'
  AND SP_USUARIO.INDMEDCAMBIADO  NOT IN (0)
  AND SP_USUARIO.CICLO BETWEEN s$cicloInicial$s  AND s$cicloFinal$s
