SELECT EXTRACT ( YEAR FROM ABONOS.FECHA)||ABONOS.PERIODO AS NOMBREDEPERIODO,
  PRIMERAPELLIDO||' '|| SEGUNDOAPELLIDO||' '|| NOMBRES  AS NOMBRECOMPLETO,
  ABONOS.VALOR   AS VLRTOTAL,
  PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(ABONOS.COMPANIA,EXTRACT (YEAR FROM ABONOS.FECHA),ABONOS.PERIODO,NULL) AS TXTNOMPERIODO,
  ABONOS.FECHA    AS FECHAABONO ,
  USUARIO.CODIGORUTA,
  ABONOS.VALORANTFACT,
  VALORABONADOPERIODO,
  ABONOS.VALOR,
  ABONOS.USUARIO,
  ABONOS.PERIODO,
  PAGOTERCERIZADO,
  PAGOCONVENIOS,
  OBSERVACION,
  ABONOS.ANO,
  ABONOS.BANCO,
  ABONOS.CICLO,
  USUARIO.CODIGOINTERNO,
  USUARIO.COMPANIA,
  ABONOS.CONSECUTIVO,
  USUARIO.CREATED_BY,
  USUARIO.DATE_CREATED,
  USUARIO.DATE_MODIFIED,
  ABONOS.FACTAUTORIZADA,
  ABONOS.FECHA,
  ABONOS.FECHAPROCESO,
  ABONOS.INDABONADO,
  ABONOS.INDAUTORIZADO,
  ABONOS.MODIFIED_BY
FROM SP_ABONOS ABONOS
LEFT JOIN SP_USUARIO USUARIO
ON ABONOS.CODIGORUTA = USUARIO.CODIGORUTA
AND ABONOS.CICLO     = USUARIO.CICLO
AND ABONOS.COMPANIA  = USUARIO.COMPANIA
WHERE TO_DATE(TO_CHAR(ABONOS.FECHA,'DD/MM/YYYY'),'DD/MM/YYYY') BETWEEN TO_DATE( 's$fechaInicial$s','DD/MM/YYYY') AND  TO_DATE('s$fechaFinal$s'
,'DD/MM/YYYY')
AND ABONOS.COMPANIA=s$compania$s

