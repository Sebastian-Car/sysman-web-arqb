SELECT USUARIO.CODIGORUTA "CODIGO RUTA",
  USUARIO.CODIGOINTERNO  "CODIGO INTERNO",
  NVL(USUARIO.PRIMERAPELLIDO,' ') ||' '|| NVL(USUARIO.SEGUNDOAPELLIDO,' ')||' '|| NVL(USUARIO.NOMBRES,' ') "NOMBRE USUARIO",
  USUARIO.USO,
  BARRIOS.NOMBRE BARRIO,
  USUARIO.ESTRATO,
  USUARIO.ESTRATOASEO "EST ASEO",
  CASE WHEN APARTAMENTO=-1 THEN 'SI' ELSE 'NO' END "TRAMO EXCEDENTE",
  CASE WHEN INDDESHABITADO=-1 THEN 'SI' ELSE 'NO' END "ASEO DESH",
  CASE WHEN USOS.NOMBRE<>'RESIDENCIAL' THEN PESOASEO ELSE 0 END "PESO ASEO",
  USUARIO.VASEOCONSUMO "ASEO CONSUMO",
  USUARIO.VASEOUNICO "ASEO UNICO",
  USUARIO.VASEODOMICILIARIO "ASEO DOMICILIARIO",
  USUARIO.VASEOBARRIDO "ASEO BARRIDO",
  USUARIO.SUBASEO  SUBSIDIO,
  USUARIO.SOBREASEO SOBREPRECIO,
  USUARIO.TOTALASEO "TOTAL ASEO"
FROM SP_USUARIO USUARIO
LEFT JOIN SP_USOS USOS
	ON USUARIO.COMPANIA = USOS.COMPANIA
	AND USUARIO.USO     = USOS.CODIGO
LEFT JOIN BARRIOS
	ON USUARIO.BARRIO=BARRIOS.CODIGO
WHERE USUARIO.COMPANIA = s$compania$s
AND USUARIO.TOTFACTURAPERACTUAL <> 0
AND USUARIO.ASEO = -1
s$condicion$s
AND USUARIO.CODIGORUTA BETWEEN s$codigoInicial$s AND s$codigoFinal$s
AND USUARIO.USO||USUARIO.ESTRATO BETWEEN s$usoIni$s||s$estratoIni$s AND s$usoFin$s
||s$estratoFin$s
AND USUARIO.BARRIO BETWEEN s$barrioIni$s AND s$barrioFin$s
s$condicionOrdenCodRuta$s 
