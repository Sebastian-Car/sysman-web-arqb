SELECT  SP_DESVIACIONES.CONSECUTIVO ITEM, 
        SP_DESVIACIONES.CICLO, 
        SP_USUARIO.CODIGOINTERNO "CODIGO INTERNO", 
        SP_USUARIO.CODIGORUTA "CODIGO RUTA", 
        SP_USUARIO.NOMBRES || ' ' || SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO  NOMBRES, 
        SP_USUARIO.DIRTECNICA DIRECCION, 
        SP_USOS.NOMBRE USO,
        SP_USUARIO.ESTRATO, 
        SP_USUARIO.MEDIDOR "Nº MEDIDOR", 
        DESVIACION.LECTURAINICIAL  "LEC ANTERIOR",
        DESVIACION.LECTURAFINAL "LEC ACTUAL",
        DESVIACION.CONSUMOREAL "CONS REAL",
        SP_DESVIACIONES.PROMEDIO "CONS PROMEDIO", 
        SP_DESVIACIONES.ESTADO "ESTADO PROCESO", 
        SP_DESVIACIONES.DECISION, 
        SP_DESVIACIONES.ANO "ANO APERTURA", 
        SP_DESVIACIONES.PERIODO "PER APERTURA", 
        TO_CHAR(SP_DESVIACIONES.FECHACREADA,'DD/MM/YYYY') "FECHA APERTURA", 
        SP_DESVIACIONES.ANOCIERRE "ANO CIERRE", 
        SP_DESVIACIONES.PERIODOCIERRE "PER CIERRE", 
        TO_CHAR(SP_DESVIACIONES.FECHACIERRE,'DD/MM/YYYY') "FECHA CIERRE"
FROM SP_DESVIACIONES 
  INNER JOIN SP_USUARIO 
    ON SP_DESVIACIONES.COMPANIA = SP_USUARIO.COMPANIA 
    AND SP_USUARIO.CICLO = SP_DESVIACIONES.CICLO 
    AND SP_USUARIO.CODIGORUTA = SP_DESVIACIONES.CODIGORUTA 
  LEFT JOIN SP_USOS
    ON SP_USUARIO.COMPANIA = SP_USOS.COMPANIA
    AND SP_USUARIO.USO = SP_USOS.CODIGO
  INNER JOIN (SELECT 
				SP_DESVIACIONES_HISTORIA.COMPANIA,
				MIN(SP_DESVIACIONES_HISTORIA.LECTURAINICIAL) KEEP (DENSE_RANK FIRST ORDER BY SP_DESVIACIONES_HISTORIA.LECTURAINICIAL)LECTURAINICIAL,
				MAX(SP_DESVIACIONES_HISTORIA.LECTURAFINAL)   KEEP (DENSE_RANK LAST ORDER BY SP_DESVIACIONES_HISTORIA.LECTURAFINAL) LECTURAFINAL,
				SUM(SP_DESVIACIONES_HISTORIA.CONSUMOACUREAL) AS CONSUMOREAL ,
				SP_DESVIACIONES_HISTORIA.DESVIACION
			FROM SP_DESVIACIONES_HISTORIA 
			GROUP BY SP_DESVIACIONES_HISTORIA.COMPANIA, 
					SP_DESVIACIONES_HISTORIA.DESVIACION)  DESVIACION
	ON SP_DESVIACIONES.COMPANIA=DESVIACION.COMPANIA 
	AND SP_DESVIACIONES.CONSECUTIVO =DESVIACION.DESVIACION
WHERE SP_DESVIACIONES.COMPANIA= s$compania$s
  AND SP_DESVIACIONES.CICLO BETWEEN s$cicloInicial$s AND s$cicloFinal$s 
  AND SP_DESVIACIONES.FECHACREADA BETWEEN  s$fechaInicial$s AND s$fechaFinal$s
  AND SP_DESVIACIONES.CODIGORUTA BETWEEN s$codigoInicial$s AND s$codigoFinal$s
  s$condicionEstado$s
  s$condicionPeriodo$s
