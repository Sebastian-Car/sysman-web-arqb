SELECT 	SP_CONCEPTOS.COMPANIA,
		SP_USUARIO.CODIGORUTA,
		PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(SP_USUARIO.COMPANIA, SP_USUARIO.ANO, SP_USUARIO.PERIODO, NULL) PERIODOS,
		SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO ||  ' ' || SP_USUARIO.NOMBRES NOMBREUSUARIO,
		SP_USUARIO.DIRTECNICA,
		SP_USUARIO.DIRGUIA,
		SP_USUARIO.LECTURA,
		SP_USUARIO.PERIODOSATRASO,
    SP_MEDIDOR.CODIGO MEDIDOR,
		SP_USUARIO.CODIGOCATASTRAL,
		SUM(CASE WHEN SP_CONCEPTOS.SERVICIO ='01' THEN VALOR_FACTURADO + SP_FACTURADO.DEUDA + VALORFINACT + VALORFINANT - VALORABONOACT - VALORABONOANT - SP_FACTURADO.CREDITOABONADO ELSE 0 END) ACU,
		SUM(CASE WHEN SP_CONCEPTOS.SERVICIO ='02' THEN VALOR_FACTURADO + SP_FACTURADO.DEUDA + VALORFINACT + VALORFINANT - VALORABONOACT - VALORABONOANT - SP_FACTURADO.CREDITOABONADO ELSE 0 END) ALC,
		SUM(CASE WHEN SP_CONCEPTOS.SERVICIO ='03' THEN VALOR_FACTURADO + SP_FACTURADO.DEUDA + VALORFINACT + VALORFINANT - VALORABONOACT - VALORABONOANT - SP_FACTURADO.CREDITOABONADO ELSE 0 END) ASE,
		SUM(CASE WHEN SP_CONCEPTOS.SERVICIO IS NULL THEN VALOR_FACTURADO + SP_FACTURADO.DEUDA + VALORFINACT + VALORFINANT - VALORABONOACT - VALORABONOANT - SP_FACTURADO.CREDITOABONADO ELSE 0 END) OTROS,
		DECODE(SP_USUARIO.ESTADO,'A','ACTIVO', 'R', 'RETIRADO', 'C', 'CORTADO', 'S', 'SUSPENDIDO') ESTADO,
		SUM(VALOR_FACTURADO + SP_FACTURADO.DEUDA + VALORFINACT + VALORFINANT - VALORABONOACT - VALORABONOANT - SP_FACTURADO.CREDITOABONADO) VLR_FACT
FROM SP_USUARIO
	INNER JOIN SP_FACTURADO
		ON SP_USUARIO.COMPANIA    = SP_FACTURADO.COMPANIA
		AND SP_USUARIO.CICLO      = SP_FACTURADO.CICLO
		AND SP_USUARIO.CODIGORUTA = SP_FACTURADO.CODIGORUTA
    		AND SP_USUARIO.ANO        = SP_FACTURADO.ANO 
    		AND SP_USUARIO.PERIODO    = SP_FACTURADO.PERIODO
	LEFT JOIN SP_CONCEPTOS	
		ON SP_CONCEPTOS.COMPANIA  = SP_FACTURADO.COMPANIA
		AND SP_CONCEPTOS.CODIGO   = SP_FACTURADO.CONCEPTO
 	 LEFT JOIN SP_MEDIDOR
   		 ON SP_USUARIO.COMPANIA     = SP_MEDIDOR.COMPANIA 
   		 AND SP_USUARIO.MEDIDOR     = SP_MEDIDOR.CONSECUTIVO
WHERE SP_USUARIO.COMPANIA = s$compania$s
 	AND SP_USUARIO.CICLO 	BETWEEN s$cicloInicial$s 	AND s$cicloFinal$s
	AND SP_USUARIO.USO 		BETWEEN s$usoInicial$s 		AND s$usoFinal$s
	AND SP_USUARIO.ESTRATO 	BETWEEN s$estratoInicial$s 	AND s$estratoFinal$s
	AND ((SP_CONCEPTOS.CODIGO BETWEEN 1 AND 48
	OR SP_CONCEPTOS.CODIGO IN (246,247,248,249))
	AND SP_CONCEPTOS.CODIGO NOT IN (12,17)) 
	s$condicionBarrio$s
    s$condicionIncluir$s
    s$condicionChapetas$s
    s$condicion$s
	AND SP_USUARIO.BANCOPERPROCESO IS NULL
GROUP BY 	SP_CONCEPTOS.COMPANIA,
			SP_USUARIO.CODIGORUTA,
			PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(SP_USUARIO.COMPANIA, SP_USUARIO.ANO, SP_USUARIO.PERIODO, NULL) ,
			SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO ||  ' ' || SP_USUARIO.NOMBRES,
			SP_USUARIO.DIRTECNICA,
			SP_USUARIO.DIRGUIA,
			SP_USUARIO.LECTURA,
			SP_USUARIO.PERIODOSATRASO,
      			SP_MEDIDOR.CODIGO,
			SP_USUARIO.CODIGOCATASTRAL,
			SP_USUARIO.ESTADO
s$orderBy$s
