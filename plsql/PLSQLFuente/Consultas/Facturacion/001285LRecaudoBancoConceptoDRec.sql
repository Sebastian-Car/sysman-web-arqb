SELECT SP_RECAUDOS.COMPANIA,
  SP_D_RECAUDO.VALORPAGOPERIODO,
  SP_D_RECAUDO.VALORDEUDA,
  SP_D_RECAUDO.VALORPAGOPERIODO+SP_D_RECAUDO.VALORDEUDA AS TOTALRECAUDADO,
  SP_RECAUDOS.BANCO,
  SP_RECAUDOS.FECHA,
  SP_CONCEPTOS.CODIGO,
  SP_CONCEPTOS.NOMBRE AS NOMBRECONCEPTO,
  SP_BANCOS.NOMBRE    AS NOMBREBANCO
FROM ((SP_RECAUDOS
LEFT JOIN SP_D_RECAUDO
ON (SP_RECAUDOS.COMPANIA       = SP_D_RECAUDO.COMPANIA)
AND (SP_RECAUDOS.FECHA         = SP_D_RECAUDO.FECHA)
AND (SP_RECAUDOS.BANCO         = SP_D_RECAUDO.BANCO)
AND (SP_RECAUDOS.NUMEROPAQUETE = SP_D_RECAUDO.NUMEROPAQUETE))
LEFT JOIN SP_BANCOS
ON (SP_RECAUDOS.COMPANIA = SP_BANCOS.COMPANIA)
AND (SP_RECAUDOS.BANCO   = SP_BANCOS.CODIGO))
LEFT JOIN SP_CONCEPTOS
ON (SP_D_RECAUDO.COMPANIA          = SP_CONCEPTOS.COMPANIA)
AND (SP_D_RECAUDO.CONCEPTO         = SP_CONCEPTOS.CODIGO)
WHERE SP_RECAUDOS.COMPANIA         = s$compania$s
AND (SP_D_RECAUDO.VALORPAGOPERIODO<> 0
OR SP_D_RECAUDO.VALORDEUDA        <>0 )
AND SP_RECAUDOS.BANCO BETWEEN s$bancoInicial$s AND s$bancoFinal$s
AND SP_RECAUDOS.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
AND (SP_CONCEPTOS.CODIGO BETWEEN 1 AND 49
OR SP_CONCEPTOS.CODIGO IN (201,202,203,204,205,206,207,246,247,248,249))
