MERGE INTO CONSULTAS FIN USING (SELECT '800496NotasEmitidasDian' INFORME ,TO_CLOB(q'[SELECT
	ESTADO_NOTAS.NUMFORMATO ,
	ESTADO_NOTAS.NUMFACTURA ,
	ESTADO_NOTAS.CLASE PREFIJO,
	ESTADO_NOTAS.ESTADO ,
	SF_OBJETO_COBRO.VALOR_TOTAL VALOR,
	ESTADO_NOTAS.FECHA ,
	SF_OBJETO_COBRO.TERCERO,
	ESTADO_DOC_DIAN.DOCUMENTKEY CUDE,
	ESTADO_NOTAS.OBSERVACION,
	ESTADO_DOC_DIAN.MENSAJE 
FROM
	ESTADO_NOTAS
INNER JOIN 
SF_OBJETO_COBRO ON
	ESTADO_NOTAS.NUMFACTURA = SF_OBJETO_COBRO.NRO_FACTURA
	INNER JOIN ESTADO_DOC_DIAN ON 
ESTADO_NOTAS.NUMFORMATO = ESTADO_DOC_DIAN.NUM_DOCUMENTO
LEFT JOIN TERCERO ON
	SF_OBJETO_COBRO.COMPANIA = TERCERO.COMPANIA
	AND SF_OBJETO_COBRO.TERCERO = TERCERO.NIT
	AND SF_OBJETO_COBRO.SUCURSAL = TERCERO.SUCURSAL
LEFT JOIN SF_FACTURA ON
	SF_FACTURA.COMPANIA = SF_OBJETO_COBRO.COMPANIA
	AND SF_FACTURA.TIPOCOBRO = SF_OBJETO_COBRO.TIPOCOBRO
	AND SF_FACTURA.ANO = SF_OBJETO_COBRO.ANO
	AND SF_FACTURA.CODIGO_COBRO = SF_OBJETO_COBRO.CODIGO_COBRO
ORDER BY
	FECHA DESC,
	ESTADO_NOTAS.NUMFACTURA DESC]') CONSULTA, 5 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);