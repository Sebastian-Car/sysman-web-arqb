        , PCK_SERVICIOS_PUBLICOS_COM8.FC_ESCRIBIRPERIODOCORTO(USUARIO.COMPANIA,(USUARIO.PERIODO-5),USUARIO.ANO) PERIODO5
        , PCK_SERVICIOS_PUBLICOS_COM8.FC_ESCRIBIRPERIODOCORTO(USUARIO.COMPANIA,(USUARIO.PERIODO-6),USUARIO.ANO) PERIODO6
        , PROBLEMA.PRIMERODEPROBLEMA PRIMERPROBLEMA
        , CASE WHEN NVL(RECAUDADOANTERIOR,0) IN (0)
               THEN PCK_SERVICIOS_PUBLICOS_COM8.FC_ULTIMOPAGO(USUARIO.COMPANIA,USUARIO.CODIGORUTA,FECHAEXPEDICION)
               ELSE RECAUDADOANTERIOR
          END ULTIMOPAGO
        , CASE WHEN  s$marca$s IN (4) 
               THEN 'FINANCIACIÓN' 
               ELSE 
                    CASE WHEN s$marca$s IN (3) OR USUARIO.ABONOACT NOT IN (0) 
                         THEN 'ABONO O PAGO PARCIAL'
                         ELSE CASE WHEN USUARIO.PERIODOSATRASO = 1
                                   THEN 'SERVICIO PARA SUSPENSIÓN'
                                   ELSE CASE WHEN USUARIO.PERIODOSATRASO > 1
                                             THEN 'SERVICIO PARA CORTE'
                                             ELSE ''
                                        END     
                              END  
                    END          
          END MARCADEAGUA 
FROM SP_USUARIO USUARIO      
    LEFT JOIN  SP_PARAMETROFACTURACION PARAMETROFACTURACION 
        ON  USUARIO.COMPANIA = PARAMETROFACTURACION.COMPANIA 
        AND USUARIO.CICLO    = PARAMETROFACTURACION.CICLO 
        AND USUARIO.ANO      = PARAMETROFACTURACION.ANO 
        AND USUARIO.PERIODO  = PARAMETROFACTURACION.PERIODO
    INNER JOIN SP_TARIFAS TARIFASCOSTOS 
        ON  USUARIO.COMPANIA = TARIFASCOSTOS.COMPANIA 
        AND USUARIO.ESTRATO  = TARIFASCOSTOS.ESTRATO 
        AND USUARIO.USO      = TARIFASCOSTOS.USO 
        AND USUARIO.ANO      = TARIFASCOSTOS.ANO 
        AND USUARIO.PERIODO  = TARIFASCOSTOS.PERIODO 
    INNER JOIN SP_USOS USOS 
        ON  USUARIO.COMPANIA = USOS.COMPANIA 
        AND USUARIO.USO      = USOS.CODIGO
    LEFT JOIN (  SELECT  COMPANIA
                       , CODIGORUTA
                       , SUM(CASE WHEN USO IN ('01')
			   	                  THEN 1
			   	                  ELSE 0
			   	             END) TOTALRESIDENCIALES
			           , SUM(CASE WHEN USO NOT IN ('01')
			         	          THEN 1
			         	          ELSE 0
			         	     END) TOTALOTROS  
			      FROM SP_UNIDADESRESIDENCIALES UNIDADESRESIDENCIALES 
			     WHERE COMPANIA   = s$compania$s
			     GROUP BY  COMPANIA
			             , CODIGORUTA) UNIDADESRESIDENCIALES
        ON  USUARIO.COMPANIA   = UNIDADESRESIDENCIALES.COMPANIA
        AND USUARIO.CODIGORUTA = UNIDADESRESIDENCIALES.CODIGORUTA   
    LEFT JOIN (SELECT   HISTORIA_CONVENIOS.COMPANIA 
                       , HISTORIA_CONVENIOS.CICLO
                       , HISTORIA_CONVENIOS.CODIGORUTA
                       , HISTORIA_CONVENIOS.ANO
                       , HISTORIA_CONVENIOS.PERIODO
                       , SUM(HISTORIA_CONVENIOS.TOTAL) TOTAL
                  FROM SP_EMPRESAS_CONVENIO EMPRESAS_CONVENIO 
                      INNER JOIN SP_HISTORIA_CONVENIOS HISTORIA_CONVENIOS 
                          ON  EMPRESAS_CONVENIO.COMPANIA = HISTORIA_CONVENIOS.COMPANIA
                          AND EMPRESAS_CONVENIO.ID       = HISTORIA_CONVENIOS.ID_EMPRESA 
                      WHERE EMPRESAS_CONVENIO.COMPANIA   = s$compania$s
                  GROUP BY  
                         HISTORIA_CONVENIOS.COMPANIA
                       , HISTORIA_CONVENIOS.CICLO
                       , HISTORIA_CONVENIOS.CODIGORUTA
                       , HISTORIA_CONVENIOS.ANO
                       , HISTORIA_CONVENIOS.PERIODO) TOTALCONVENIOS
        ON  USUARIO.COMPANIA = TOTALCONVENIOS.COMPANIA 
        AND USUARIO.CICLO    = TOTALCONVENIOS.CICLO 
        AND USUARIO.ANO      = TOTALCONVENIOS.ANO 
        AND USUARIO.PERIODO  = TOTALCONVENIOS.PERIODO   
        AND USUARIO.CODIGORUTA  = TOTALCONVENIOS.CODIGORUTA  
    LEFT JOIN SP_HISTORIA_EXTERNA HISTORIA_EXTERNA 
        ON  USUARIO.Compania       = HISTORIA_EXTERNA.COMPANIA
        AND USUARIO.PERIODO        = HISTORIA_EXTERNA.PERIODO 
        AND USUARIO.ANO            = HISTORIA_EXTERNA.ANO 
        AND USUARIO.CICLO          = HISTORIA_EXTERNA.CICLO
        AND USUARIO.CODIGORUTA     = HISTORIA_EXTERNA.CODIGORUTA 
        AND USUARIO.EMPRESAASEOEXT = HISTORIA_EXTERNA.ID_EMPRESA 
    INNER JOIN SP_ESTRATOS ESTRATOS 
        ON  USUARIO.COMPANIA = ESTRATOS.COMPANIA
        AND USUARIO.ESTRATO  = ESTRATOS.CODIGO  
        AND USUARIO.USO      = ESTRATOS.USO
    LEFT JOIN SP_MEDIDOR MEDIDOR
        ON  USUARIO.COMPANIA = MEDIDOR.COMPANIA 
        AND USUARIO.MEDIDOR  = MEDIDOR.CONSECUTIVO 
    LEFT JOIN (SELECT   FACTURADO.COMPANIA        
                      , FACTURADO.CICLO        
                      , FACTURADO.CODIGORUTA        
                      , FACTURADO.ANO        
                      , FACTURADO.PERIODO          
                      , SUM(FACTURADO.VALOR_FACTURADO) VALOR_FACTURADO        
                 FROM SP_FACTURADO FACTURADO     
                     INNER JOIN SP_CONCEPTOS CONCEPTOS        
                         ON  FACTURADO.COMPANIA = CONCEPTOS.COMPANIA      
                         AND FACTURADO.CONCEPTO = CONCEPTOS.CODIGO      
                     LEFT JOIN SP_FINANCIABLES FINANCIABLES        
                         ON  FACTURADO.COMPANIA = FINANCIABLES.COMPANIA         
                         AND FACTURADO.CICLO      = FINANCIABLES.CICLO         
                         AND FACTURADO.CODIGORUTA = FINANCIABLES.CODIGORUTA       
                         AND FACTURADO.ANO        = FINANCIABLES.ANO         
                         AND FACTURADO.PERIODO    = FINANCIABLES.PERIODO   
                         AND FACTURADO.CONCEPTO   = FINANCIABLES.CONCEPTO 
                WHERE FACTURADO.COMPANIA= s$compania$s   
                  AND ((FACTURADO.CONCEPTO NOT IN (1,2,3,4,5,20,21,22,33,46,48)  		 
                       AND FACTURADO.CONCEPTO<=50)       
                      OR FACTURADO.CONCEPTO IN (246,247,248,249))  
                  AND FACTURADO.VALOR_FACTURADO NOT IN (0)
                GROUP BY  
                        FACTURADO.COMPANIA        
                      , FACTURADO.CICLO        
                      , FACTURADO.CODIGORUTA        
                      , FACTURADO.ANO        
                      , FACTURADO.PERIODO) TOTALFACTURA 
        ON  USUARIO.COMPANIA   = TOTALFACTURA.COMPANIA 
        AND USUARIO.ANO        = TOTALFACTURA.ANO  
        AND USUARIO.CICLO      = TOTALFACTURA.CICLO 
        AND USUARIO.PERIODO    = TOTALFACTURA.PERIODO 
        AND USUARIO.CODIGORUTA = TOTALFACTURA.CODIGORUTA    
    LEFT JOIN ( SELECT   USUARIO_PROBLEMA.COMPANIA
    	               , USUARIO_PROBLEMA.ANO
    	               , USUARIO_PROBLEMA.PERIODO
                       , USUARIO_PROBLEMA.CICLO
                       , USUARIO_PROBLEMA.CODIGORUTA
                       , MIN(USUARIO_PROBLEMA.PROBLEMA) PRIMERODEPROBLEMA
                       , PROBLEMA.NOMBRE
                       , USUARIO_PROBLEMA.CLASE 
                  FROM SP_PROBLEMA PROBLEMA
                      INNER JOIN SP_USUARIO_PROBLEMA USUARIO_PROBLEMA 
                          ON  PROBLEMA.COMPANIA              = USUARIO_PROBLEMA.COMPANIA
                          AND PROBLEMA.CLASEPROBLEMA        = USUARIO_PROBLEMA.CLASE
                          AND PROBLEMA.CODIGO               = USUARIO_PROBLEMA.PROBLEMA  
                 WHERE USUARIO_PROBLEMA.COMPANIA = s$compania$s 
                   AND USUARIO_PROBLEMA.CLASE    = 'AFR'
                 GROUP BY 
                         USUARIO_PROBLEMA.COMPANIA
                       , USUARIO_PROBLEMA.ANO
                       , USUARIO_PROBLEMA.PERIODO
                       , USUARIO_PROBLEMA.CICLO
                       , USUARIO_PROBLEMA.CODIGORUTA
                       , PROBLEMA.NOMBRE
                       , USUARIO_PROBLEMA.CLASE) PROBLEMA
        ON  USUARIO.COMPANIA   = PROBLEMA.COMPANIA 
        AND USUARIO.ANO        = PROBLEMA.ANO  
        AND USUARIO.CICLO      = PROBLEMA.CICLO 
        AND USUARIO.PERIODO    = PROBLEMA.PERIODO 
        AND USUARIO.CODIGORUTA = PROBLEMA.CODIGORUTA   
WHERE USUARIO.COMPANIA   = s$compania$s
  AND USUARIO.CICLO      = s$ciclo$s
  AND USUARIO.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 
  AND USUARIO.ESTADO NOT IN ('R') 
  AND (CASE WHEN s$marca$s IN (3) OR USUARIO.ABONOACT NOT IN (0) 
            THEN USUARIO.ABONOACT
            ELSE USUARIO.TOTFACTURAPERACTUAL
       END + NVL(HISTORIA_EXTERNA.VALORASEO,0))>  s$deudaMayorA$s
ORDER BY USUARIO.CODIGORUTA 
