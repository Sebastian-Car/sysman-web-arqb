SELECT TRUNC(SP_COBROSPERSUASIVOS.FECHAACTA) FECHAACTA,
       SP_COBROSPERSUASIVOS.CICLO,
       SP_COBROSPERSUASIVOS.CODIGORUTA,
       SP_USUARIO.NOMBRES || ' ' || SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO AS NOMBRE,
       SP_USUARIO.DIRTECNICA,
       SP_COBROSPERSUASIVOS.PERIODOSATRASO,
       SP_COBROSPERSUASIVOS.OBSERVACION,
       SP_COBROSPERSUASIVOS.DEUDA,
       CASE SP_COBROSPERSUASIVOS.ESTADO 
       WHEN 'C' THEN 's$cerrado$s'
       WHEN 'T' THEN 's$coactivo$s'
       ELSE 's$abierto$s' END AS ESTADO
  FROM SP_COBROSPERSUASIVOS
       INNER JOIN SP_USUARIO 
       ON (SP_COBROSPERSUASIVOS.COMPANIA = SP_USUARIO.COMPANIA)
       AND (SP_COBROSPERSUASIVOS.CICLO = SP_USUARIO.CICLO)
       AND  (SP_COBROSPERSUASIVOS.CODIGORUTA = SP_USUARIO.CODIGORUTA)
 WHERE SP_COBROSPERSUASIVOS.COMPANIA = s$compania$s
   AND TRUNC(SP_COBROSPERSUASIVOS.FECHAACTA) BETWEEN s$fechaInicial$s AND s$fechaFinal$s
