SELECT 
	SS.CLASESOLICITUD,
	S.NOMBRE NOMBRESERVICIO,
	TO_CHAR(FECHASOLICITUD,'DD/MM/YYYY') FECHASOLICITUD, 
	NUMERO, 
	DIRTECNICA, 
	NVL(PRIMERAPELLIDO,'') ||' '|| NVL(SEGUNDOAPELLIDO,'') ||' '|| NVL(NOMBRES,'') AS NOMBRECOMPLETO,
	ESTADOAPROBADO, 
	USUARIO, 
	SUM(NVL(VALOR,0)) AS SUMADEVALOR, 
	SUM(NVL(INICIAL,0)) AS SUMADEINICIAL, 
	MIN(NVL(CUOTAS,0))  KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) AS PRIMERODECUOTAS, 
	SS.NIT, 
	(CASE WHEN ACTIVADO IN (0)
    	THEN ' '
      	ELSE 'X'
	END) ACTIVADO,
	ESTRATO
FROM SP_SOLICITUDSERVICIO SS
	LEFT JOIN SP_SOLICITUDFINANCIABLES SF
		ON SS.COMPANIA 		  = SF.COMPANIA
		AND SS.CLASESOLICITUD = SF.CLASESOLICITUD 
		AND NUMERO 		      = SOLICITUD
  	INNER JOIN SP_SOLICITUDPORSERVICIO S
    	ON S.COMPANIA = SS.COMPANIA
    	AND S.CODIGO  = SS.CLASESOLICITUD
WHERE SS.COMPANIA=s$compania$s 
	AND SS.FECHASOLICITUD BETWEEN s$fechaInicial$s AND s$fechaFinal$s
                     AND SS.CLASESOLICITUD = '0'||s$mostrar$s
GROUP BY 
	SS.CLASESOLICITUD, 
	S.NOMBRE,
	SS.FECHASOLICITUD, 
	NUMERO, 
	NVL(PRIMERAPELLIDO,'') ||' '|| NVL(SEGUNDOAPELLIDO,'') ||' '|| NVL(NOMBRES,''), 
	ESTADOAPROBADO, 
	DIRTECNICA, 
	NIT, 
	USUARIO, 
	ACTIVADO, 
	ESTRATO, 
  	NUMERO
ORDER BY 
	SS.CLASESOLICITUD, 
	SS.FECHASOLICITUD, 
	NUMERO
