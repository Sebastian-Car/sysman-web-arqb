SELECT SP_D_ABONOS.COMPANIA, 
       SP_D_ABONOS.CICLO, 
       SP_D_ABONOS.CODIGORUTA, 
       SP_D_ABONOS.ANO, 
       SP_D_ABONOS.PERIODO, 
       SP_D_ABONOS.CONCEPTO, 
       SP_D_ABONOS.VALOR  TOTALABONO, 
       SP_USUARIO.PRIMERAPELLIDO ||' '|| SP_USUARIO.SEGUNDOAPELLIDO ||' '||  SP_USUARIO.NOMBRES NOMBRES, 
       SP_USUARIO.CODIGOINTERNO, 
       SP_D_ABONOS.ANO || SP_D_ABONOS.PERIODO  ANOPERIODO, 
       SP_D_ABONOS.CONSECUTIVO, 
       SP_D_ABONOS.VALORACT, 
       SP_D_ABONOS.VALORANT, 
       VALORANT + VALORACT  TOTALAB 
FROM  SP_D_ABONOS 
INNER JOIN SP_USUARIO
  ON   SP_D_ABONOS.COMPANIA = SP_USUARIO.COMPANIA
  AND  SP_D_ABONOS.CICLO = SP_USUARIO.CICLO 
  AND  SP_D_ABONOS.CODIGORUTA = SP_USUARIO.CODIGORUTA
INNER JOIN SP_CONCEPTOS 
  ON    SP_D_ABONOS.COMPANIA = SP_CONCEPTOS.COMPANIA
  AND   SP_D_ABONOS.CONCEPTO = SP_CONCEPTOS.CODIGO
WHERE SP_D_ABONOS.COMPANIA =  s$compania$s 
  AND   SP_D_ABONOS.CICLO = s$ciclo$s
  AND  SP_D_ABONOS.CODIGORUTA = 's$codigoRuta$s'
  AND  (SP_D_ABONOS.ANO || SP_D_ABONOS.PERIODO >= SP_USUARIO.ANO || SP_USUARIO.PERIODO) 
  AND  (VALORANT + VALORACT NOT IN (0))
