SELECT USUARIO.CODIGORUTA,
  USUARIO.CODIGOINTERNO,
  USUARIO.NIT,
  USUARIO.PRIMERAPELLIDO || ' ' || USUARIO.SEGUNDOAPELLIDO || ' ' || USUARIO.NOMBRES AS NOMBRE,
  USUARIO.DIRTECNICA,
  USUARIO.USO,
  USUARIO.LECTURA1,
  USUARIO.LECTURA,
  USUARIO.CONSUMOPROM,
  USUARIO.CONSUMO,
  USUARIO.ACUMULADO,
  PROBLEMA.NOMBRE AS PROBLEMA
FROM SP_USUARIO USUARIO
INNER JOIN (SP_PROBLEMA PROBLEMA
            INNER JOIN SP_USUARIO_PROBLEMA USUARIO_PROBLEMA
               ON PROBLEMA.COMPANIA      = USUARIO_PROBLEMA.COMPANIA
              AND PROBLEMA.CLASEPROBLEMA = USUARIO_PROBLEMA.CLASE
              AND PROBLEMA.CODIGO        = USUARIO_PROBLEMA.PROBLEMA)
   ON USUARIO.COMPANIA       = USUARIO_PROBLEMA.COMPANIA    
  AND USUARIO.CICLO          = USUARIO_PROBLEMA.CICLO
  AND USUARIO.CODIGORUTA     = USUARIO_PROBLEMA.CODIGORUTA
  AND USUARIO.ANO          = USUARIO_PROBLEMA.ANO
  AND USUARIO.PERIODO     = USUARIO_PROBLEMA.PERIODO
WHERE USUARIO.COMPANIA       = :COMPANIA
  AND USUARIO.CICLO          = :CICLO
  AND USUARIO.TIPOCALCULO    = 'P'
  AND USUARIO.ESTADO        <> 'R'  
ORDER BY USUARIO.CODIGORUTA
