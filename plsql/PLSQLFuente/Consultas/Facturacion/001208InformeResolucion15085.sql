SELECT  SP_ESTRATOS.CLASIFICACIONSUIASEO,
  QRY_USUARIOASEO_EST.ANO,
  QRY_USUARIOASEO_EST.CICLO,
  QRY_USUARIOASEO_EST.PERIODO,
  QRY_USUARIOASEO_EST.PERIODOUNICO,
  SUM(QRY_USUARIOASEO_EST.SUMADENUMUSUARIOS) AS NUMUSUARIOS,
  SUM( CASE WHEN SP_USOS.NOMBRE <> 'RESIDENCIAL' THEN SUMADESUMADEPESOASEO ELSE 0 END ) AS SUMADEPESOASEO,
  QRY_DESHABITADO_ASEO.SUSCASEODESH AS SUSCASEODESH,
  SUM(QRY_USUARIOASEO_EST.CANTIDADMULT) AS CANTIDADMULT,
  SUM(QRY_USUARIO_TOTFACT.VLRFACTURADOASEO)AS VLRFACTURADOASEO,
  SUM(QRY_USUARIO_TOTFACT.VLRFACTURADOASEOTOT) AS VLRFACTURADOASEOTOT,
  SUM(QRY_USUARIO_TOTFACT.VLRRECAUDADOASEO) AS VLRRECAUDADOASEO,
  SUM(QRY_USUARIO_TOTFACT.VLRRECAUDADOASEOTOT) AS VLRRECAUDADOASEOTOT,
  CLASIFICACIONSUIASEO  AS ORDEN_SUI,
  SUM( CASE WHEN SP_ESTRATOS.CLASIFICACIONSUIASEO < '7' THEN 0 ELSE QRY_USUARIOASEO_EST.USUARIOSAFORADOS END ) AS USUARIOSAFORADO,
  SP_USOS.NOMBRE
FROM
  (SELECT SP_ESTADISTICAS_CONSUMO.COMPANIA,
    SUM(SP_ESTADISTICAS_CONSUMO.CANTUSUARIOSASEO) AS SUMADENUMUSUARIOS,
    SUM( CASE WHEN SERVICIO IN(03) THEN NVL(SP_MULTIUSUARIOS.CANTIDAD,0) ELSE 0 END) AS CANTIDADMULT,
    SP_ESTADISTICAS_CONSUMO.USO,
    (SP_ESTADISTICAS_CONSUMO.ANO  || SP_ESTADISTICAS_CONSUMO.PERIODO) AS PERIODOUNICO,
    SUM(SP_ESTADISTICAS_CONSUMO.PESOASEO) AS SUMADESUMADEPESOASEO,
    SP_ESTADISTICAS_CONSUMO.ANO,
    SP_ESTADISTICAS_CONSUMO.PERIODO,
    SP_ESTADISTICAS_CONSUMO.ESTRATO,
    SP_ESTADISTICAS_CONSUMO.CICLO,
    SUM(NVL(SP_MULTIUSUARIOS.PESOASEO,0)) AS PESOASEOMULT,
	SUM(SP_ESTADISTICAS_CONSUMO.CANTUSUARIOASEODESH) AS SUSCASEODESH,
    SP_ESTADISTICAS_CONSUMO.CANTUSUARIOSAFORO AS USUARIOSAFORADOS
     FROM SP_ESTADISTICAS_CONSUMO
     LEFT JOIN  SP_MULTIUSUARIOS
     ON (SP_ESTADISTICAS_CONSUMO.COMPANIA  = SP_MULTIUSUARIOS.COMPANIA)
    AND (SP_ESTADISTICAS_CONSUMO.USO = SP_MULTIUSUARIOS.USO)
    AND (SP_ESTADISTICAS_CONSUMO.ESTRATO = SP_MULTIUSUARIOS.ESTRATO)
     AND (SP_ESTADISTICAS_CONSUMO.CICLO = SP_MULTIUSUARIOS.CICLO)
  WHERE SP_ESTADISTICAS_CONSUMO.COMPANIA= s$compania$s
    AND (SP_ESTADISTICAS_CONSUMO.ANO || SP_ESTADISTICAS_CONSUMO.PERIODO)
    BETWEEN s$periodoInicialCompleto$s AND s$periodoFinalCompleto$s 
  GROUP BY SP_ESTADISTICAS_CONSUMO.COMPANIA,
    SP_ESTADISTICAS_CONSUMO.USO,
    SP_ESTADISTICAS_CONSUMO.ANO,
    SP_ESTADISTICAS_CONSUMO.PERIODO,
    SP_ESTADISTICAS_CONSUMO.ESTRATO,
    SP_ESTADISTICAS_CONSUMO.CICLO,
    SP_ESTADISTICAS_CONSUMO.CANTUSUARIOSAFORO
  ) QRY_USUARIOASEO_EST
LEFT JOIN
  (SELECT SP_ESTADISTICAS_CONSUMO.COMPANIA,
    SP_ESTADISTICAS_CONSUMO.CICLO,
    SP_ESTADISTICAS_CONSUMO.ANO,
    SP_ESTADISTICAS_CONSUMO.PERIODO,
    SP_ESTADISTICAS_CONSUMO.USO,
    SP_ESTADISTICAS_CONSUMO.ESTRATO,
    SUM(SP_ESTADISTICAS_CONSUMO.CANTUSUARIOASEODESH) AS SUSCASEODESH
  FROM SP_ESTADISTICAS_CONSUMO
  WHERE SP_ESTADISTICAS_CONSUMO.COMPANIA = s$compania$s
    AND (SP_ESTADISTICAS_CONSUMO.ANO || SP_ESTADISTICAS_CONSUMO.PERIODO)
    BETWEEN s$periodoInicialCompleto$s AND s$periodoFinalCompleto$s 
  GROUP BY SP_ESTADISTICAS_CONSUMO.COMPANIA,
    SP_ESTADISTICAS_CONSUMO.CICLO,
    SP_ESTADISTICAS_CONSUMO.ANO,
    SP_ESTADISTICAS_CONSUMO.PERIODO,
    SP_ESTADISTICAS_CONSUMO.USO,
    SP_ESTADISTICAS_CONSUMO.ESTRATO
  ) QRY_DESHABITADO_ASEO
  ON QRY_USUARIOASEO_EST.COMPANIA = QRY_DESHABITADO_ASEO.COMPANIA
  AND QRY_USUARIOASEO_EST.CICLO    = QRY_DESHABITADO_ASEO.CICLO
  AND QRY_USUARIOASEO_EST.ANO      = QRY_DESHABITADO_ASEO.ANO
  AND QRY_USUARIOASEO_EST.PERIODO  = QRY_DESHABITADO_ASEO.PERIODO
  AND QRY_USUARIOASEO_EST.ESTRATO  = QRY_DESHABITADO_ASEO.ESTRATO
  AND QRY_USUARIOASEO_EST.USO       = QRY_DESHABITADO_ASEO.USO
LEFT JOIN
  (SELECT SP_ESTADISTICAS_FACTURACION.COMPANIA,
  	SP_ESTADISTICAS_FACTURACION.CICLO,
    SP_ESTADISTICAS_FACTURACION.ANO,
    SP_ESTADISTICAS_FACTURACION.PERIODO,
    SP_ESTADISTICAS_FACTURACION.ESTRATO,
    SP_ESTADISTICAS_FACTURACION.USO,
    SUM(SP_ESTADISTICAS_FACTURACION.VALOR_FACTURADO)  AS VLRFACTURADOASEO,
    SUM(VALOR_FACTURADO + DEUDA)                      AS VLRFACTURADOASEOTOT,
    SUM(SP_ESTADISTICAS_FACTURACION.RECAUDADOPERIODO) AS VLRRECAUDADOASEO,
    SUM(RECAUDADODEUDA + RECAUDADOPERIODO)            AS VLRRECAUDADOASEOTOT
  FROM SP_ESTADISTICAS_FACTURACION
  INNER JOIN SP_CONCEPTOS
    ON  SP_ESTADISTICAS_FACTURACION.COMPANIA = SP_CONCEPTOS.COMPANIA
    AND SP_ESTADISTICAS_FACTURACION.CONCEPTO  = SP_CONCEPTOS.CODIGO
  WHERE SP_CONCEPTOS.SERVICIO  IN(03)
    AND SP_ESTADISTICAS_FACTURACION.COMPANIA = s$compania$s
    AND (SP_ESTADISTICAS_FACTURACION.ANO || SP_ESTADISTICAS_FACTURACION.PERIODO)
    BETWEEN s$periodoInicialCompleto$s  AND s$periodoFnalCompleto$s 
  GROUP BY SP_ESTADISTICAS_FACTURACION.COMPANIA,
    SP_ESTADISTICAS_FACTURACION.CICLO,
    SP_ESTADISTICAS_FACTURACION.ANO,
    SP_ESTADISTICAS_FACTURACION.PERIODO,
    SP_ESTADISTICAS_FACTURACION.ESTRATO,
    SP_ESTADISTICAS_FACTURACION.USO
  ) QRY_USUARIO_TOTFACT 
  ON  QRY_USUARIOASEO_EST.COMPANIA  = QRY_USUARIO_TOTFACT.COMPANIA
  AND QRY_USUARIOASEO_EST.CICLO = QRY_USUARIO_TOTFACT.CICLO
  AND QRY_USUARIOASEO_EST.ANO   = QRY_USUARIO_TOTFACT.ANO
  AND QRY_USUARIOASEO_EST.ESTRATO  = QRY_USUARIO_TOTFACT.ESTRATO
  AND QRY_USUARIOASEO_EST.PERIODO  = QRY_USUARIO_TOTFACT.PERIODO
  AND QRY_USUARIOASEO_EST.USO = QRY_USUARIO_TOTFACT.USO
INNER JOIN SP_ESTRATOS
  ON  QRY_USUARIOASEO_EST.COMPANIA = SP_ESTRATOS.COMPANIA
  AND QRY_USUARIOASEO_EST.USO     = SP_ESTRATOS.USO
  AND QRY_USUARIOASEO_EST.ESTRATO = SP_ESTRATOS.CODIGO
INNER JOIN SP_USOS
  ON  QRY_USUARIOASEO_EST.COMPANIA = SP_USOS.COMPANIA
  AND QRY_USUARIOASEO_EST.USO  = SP_USOS.CODIGO
  GROUP BY SP_ESTRATOS.CLASIFICACIONSUIASEO,
  QRY_USUARIOASEO_EST.ANO,
  QRY_USUARIOASEO_EST.CICLO,
  QRY_USUARIOASEO_EST.PERIODO,
  QRY_USUARIOASEO_EST.PERIODOUNICO,
  QRY_DESHABITADO_ASEO.SUSCASEODESH,
  SP_USOS.NOMBRE
ORDER BY QRY_USUARIOASEO_EST.ANO,
  QRY_USUARIOASEO_EST.CICLO,
  QRY_USUARIOASEO_EST.PERIODO,
  CLASIFICACIONSUIASEO
