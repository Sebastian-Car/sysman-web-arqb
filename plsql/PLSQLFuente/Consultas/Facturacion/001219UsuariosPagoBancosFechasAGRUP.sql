SELECT SUM(SP_PAGO.VALORPAGO) AS SUMADEVALORPAGO,
  UPPER(SP_BANCOS.NOMBRE)  AS NOMBREBANCO,
  COUNT(SP_PAGO.CODIGOINTERNO) AS CUENTADECODIGOINTERNO,
 UPPER(SP_PAGO.USUARIO) AS NOMBRELARGO 
FROM SP_PAGO
LEFT JOIN SP_BANCOS
  ON  SP_PAGO.COMPANIA = SP_BANCOS.COMPANIA
  AND SP_PAGO.BANCO   = SP_BANCOS.CODIGO
WHERE SP_PAGO.COMPANIA= 001
  AND SP_PAGO.BANCO
  BETWEEN 's$bancoInicial$s' AND 's$bancoFinal$s' 
  AND SP_PAGO.FECHA 
  BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY')  AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
GROUP BY SP_Bancos.NOMBRE, SP_PAGO.USUARIO
