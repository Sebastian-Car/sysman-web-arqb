SELECT CODIGORUTA, 
  NVL(PRIMERAPELLIDO,' ') PRIMERAPELLIDO, 
  NVL(SEGUNDOAPELLIDO,' ') SEGUNDOAPELLIDO, 
  NVL(NOMBRES,' ') NOMBRES, 
  DIRTECNICA, 
  TO_CHAR(FECHARENTRADANUEVOUSUARIO,'DD/MM/YYYY') FECHARENTRADANUEVOUSUARIO
FROM SP_USUARIO
WHERE COMPANIA    =s$compania$s
  AND CICLO       =s$ciclo$s
  AND TOTALIZADOR ='T'
ORDER BY COMPANIA, CICLO, CODIGORUTA
