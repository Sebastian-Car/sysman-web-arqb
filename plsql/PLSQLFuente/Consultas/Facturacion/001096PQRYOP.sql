SELECT COMPANIA.NOMBRE NOMBRECOMPANIA,
       CIUDAD.NOMBRE CIUDAD,
       SP_USUARIO.NOMBRES||' '||SP_USUARIO.PRIMERAPELLIDO||' '||SP_USUARIO.SEGUNDOAPELLIDO NOMBREUSUARIO,
       SP_USUARIO.DIRTECNICA, 
       SP_USUARIO.TELEFONO,
       COMPANIA.CODIGO,
       SP_D_ORDENTRABAJO.OBSERVACIONES, 
       SP_D_ORDENTRABAJO.FECHASOLUCION,
       SP_D_ORDENTRABAJO.SOLUCION, 
       SP_PROBLEMA.NOMBRE NOMBREPROBLEMA,
       SP_USUARIO.CODIGOINTERNO, 
       SP_USOS.NOMBRE NOM_USO,
       SP_ESTRATOS.NOMBRE NOM_ESTRATO, 
       SP_USUARIO.MEDIDOR,
       SP_USUARIO.LECTURA1,
       SP_USUARIO.LECTURA, 
       SP_USUARIO.CONSUMO,
       SP_USUARIO.PERIODOSATRASO,
       SP_USUARIO.TIPODOCUMENTO,
       SP_USUARIO.NIT,
       SP_USUARIO.TELEFONO TEL_SP_USUARIO,
       SP_ORDENTRABAJO.CLASEDOC,
       SP_ORDENTRABAJO.NUMORDEN,
       SP_ORDENTRABAJO.CICLO,
       SP_ORDENTRABAJO.CODIGORUTA,
       SP_ORDENTRABAJO.PRESENTACION,
       SP_ORDENTRABAJO.ENVIOOFICINA,
       SP_ORDENTRABAJO.NECESITAVISITA,
       SP_ORDENTRABAJO.DEPENDENCIAENV,
       SP_ORDENTRABAJO.RECIBIDOPOR,
       SP_ORDENTRABAJO.OPERADOR,
       SP_ORDENTRABAJO.AFORADOR,
       SP_ORDENTRABAJO.PRIMERAPELLIDO,
       SP_ORDENTRABAJO.SEGUNDOAPELLIDO,
       SP_ORDENTRABAJO.NOMBRES,
       SP_ORDENTRABAJO.DIRTECNICA DIRTECNICAOT,
       SP_ORDENTRABAJO.DIRGUIA,
       SP_ORDENTRABAJO.TELEFONO TELEFONOOT,
       SP_ORDENTRABAJO.ORDENGENERADA,
       SP_ORDENTRABAJO.REMISION,
       SP_ORDENTRABAJO.RADICADO,
       SP_ORDENTRABAJO.SOLICITANTE,
       SP_ORDENTRABAJO.NUMORDENEXT,
       SP_ORDENTRABAJO.OFPRESENTACION,
       SP_ORDENTRABAJO.ACTIVO,
       SP_ORDENTRABAJO.NOREPORTARSUI,
       SP_ORDENTRABAJO.TIPOREQUERIMIENTO,
       CASE SP_ORDENTRABAJO.TIPOREQUERIMIENTO WHEN 'C' THEN 'Petición'
                                              WHEN 'Q' THEN 'Reclamación'
                                              WHEN 'RE' THEN 'Reclamación'
                                              WHEN 'R' THEN 'Recurso de Reposición'
                                              WHEN 'P' THEN 'Petición'
                                              WHEN 'RA' THEN 'Recurso de Reposición y Subsidiario de Apelación'
      END TIPOREQUERIMIENTO_NOM,
       SP_ORDENTRABAJO.NUMORDENT,
       SP_ORDENTRABAJO.CAUSALREFAC,
       SP_ORDENTRABAJO.NUM_REFAC,
       SP_ORDENTRABAJO.FACTURA,
       SP_ORDENTRABAJO.SOLICITANTE_NIT,
       SP_ORDENTRABAJO.SUCURSAL,
       SP_ORDENTRABAJO.SOLICITANTE_TEL,
       SP_ORDENTRABAJO.SOLICITANTE_DIR,
       SP_ORDENTRABAJO.INDENVIARSOLCITUD,
       SP_ORDENTRABAJO.INDASIGNADO,
       SP_ORDENTRABAJO.AFORADORSITIO,
       SP_ORDENTRABAJO.EMAIL,
       SP_ORDENTRABAJO.HORASOLICITUD,
       SP_ORDENTRABAJO.FECHAENVIO,
       SP_ORDENTRABAJO.FECHASOLICITUD
FROM SP_ORDENTRABAJO 
  INNER JOIN SP_USUARIO 
     ON SP_ORDENTRABAJO.COMPANIA = SP_USUARIO.COMPANIA
    AND SP_ORDENTRABAJO.CICLO = SP_USUARIO.CICLO 
    AND SP_ORDENTRABAJO.CODIGORUTA = SP_USUARIO.CODIGORUTA
  INNER JOIN COMPANIA
     ON SP_ORDENTRABAJO.COMPANIA = COMPANIA.CODIGO
  INNER JOIN CIUDAD
     ON COMPANIA.PAIS = CIUDAD.PAIS
    AND COMPANIA.DEPARTAMENTO = CIUDAD.DEPARTAMENTO
    AND COMPANIA.CIUDAD = CIUDAD.CODIGO
  LEFT JOIN SP_D_ORDENTRABAJO 
     ON SP_ORDENTRABAJO.COMPANIA = SP_D_ORDENTRABAJO.COMPANIA
    AND SP_ORDENTRABAJO.CLASEDOC = SP_D_ORDENTRABAJO.CLASEDOC
    AND SP_ORDENTRABAJO.NUMORDEN = SP_D_ORDENTRABAJO.ORDENTRABAJO
  INNER JOIN SP_PROBLEMA 
     ON SP_D_ORDENTRABAJO.COMPANIA = SP_PROBLEMA.COMPANIA
    AND SP_D_ORDENTRABAJO.CLASEPROBLEMA = SP_PROBLEMA.CLASEPROBLEMA
    AND SP_D_ORDENTRABAJO.PROBLEMA = SP_PROBLEMA.CODIGO
  INNER JOIN SP_USOS 
     ON SP_USUARIO.COMPANIA = SP_USOS.COMPANIA
    AND SP_USUARIO.USO = SP_USOS.CODIGO
  INNER JOIN SP_ESTRATOS 
     ON SP_USUARIO.COMPANIA = SP_ESTRATOS.COMPANIA
    AND SP_USUARIO.USO = SP_ESTRATOS.USO
    AND SP_USUARIO.ESTRATO = SP_ESTRATOS.CODIGO
WHERE SP_ORDENTRABAJO.COMPANIA = s$compania$s
  AND SP_ORDENTRABAJO.CLASEDOC = 's$claseDoc$s'
  AND SP_ORDENTRABAJO.NUMORDEN = s$numOrden$s
ORDER BY SP_ORDENTRABAJO.NUMORDEN
