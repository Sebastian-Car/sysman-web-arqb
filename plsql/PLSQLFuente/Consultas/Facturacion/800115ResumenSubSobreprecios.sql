SELECT ESTRATOS.NOMBRE AS ESTRATO,  
   USOS.NOMBRE  AS USO,  
   SUM (USUARIO.SUBFIJO) AS FIJO,  
   SUM (USUARIO.SUBCONSUMOAC) AS SUBCONSUMOACU,  
   SUM (USUARIO.SUBSINMEDICION) AS SUBSINMEDICACU,  
   SUM (USUARIO.SUBACUEDUCTO) AS TOTALSUBACU,  
   SUM (USUARIO.SUBFIJOALC)   AS SUBFIJOALC,  
   SUM (USUARIO.SUBCONSUMOAL) AS SUBCONSUMOALC,  
   SUM (USUARIO.SUBALCSINMEDICION)  AS SUBSINMEDIALC,  
   SUM (USUARIO.SUBALCANTARILLADO) AS TOTALSUBALC,  
   SUM (USUARIO.SUBASEO) AS SUBSASEO,  
   SUM (USUARIO.SUBBARR_LIM) AS SUBBARRIDOASEO,  
   SUM (USUARIO.SUBDISP_FINAL) AS SUBTRATAFINALASEO,  
   SUM (USUARIO.SUBMANEJO_REC) AS SUBCOMERASEO,  
   SUM (USUARIO.SUBRECOLECCION) AS SUBRECOLECASEO,  
   SUM (USUARIO.SUBTRAMO) AS SUBTEXECEASEO,  
   SUM (USUARIO.SOBREFIJO) AS SOBFIJOACU,  
   SUM (USUARIO.SOBRECONSUMOAC) AS SOBCONSUMOACU,  
   SUM (USUARIO.SOBRESINMEDICION) AS SOBSMEDIACU,  
   SUM (USUARIO.SOBREACUEDUCTO) AS TOTALSOBACU,  
   SUM (USUARIO.SOBREFIJOALC) AS SOBDIJOALCAN,  
   SUM (USUARIO.SOBRECONSUMOAL)  AS SOBCONSUMOALC,  
   SUM (USUARIO.SOBREALCSINMEDICION) AS SOBSINMEDIALC,  
   SUM (USUARIO.SOBREALCANTARILLADO) AS TOTALSOBALC,  
   SUM (USUARIO.SOBREASEO) AS SOBASEO, 
   SUM (USUARIO.SOBBARR_LIM) AS SOBBARRIDOASEO,  
   SUM (USUARIO.SOBDISP_FINAL) AS SOBTRATAFINALASEO,  
   SUM (USUARIO.SOBMANEJO_REC)  AS SOBCOMERASEO,  
   SUM (USUARIO.SOBRECOLECCION) AS SOBRECOLECASEO,  
   SUM (USUARIO.SOBTRAMO) AS SOBTEXECEASEO,  
   SUM (USUARIO.SUBSIDIO) AS TOTALSUBSIDIOS,  
   SUM (USUARIO.SOBREPRECIO) AS TOTALSOBRE  
FROM SP_USUARIO USUARIO  
  INNER JOIN SP_ESTRATOS ESTRATOS  
     ON  USUARIO.COMPANIA = ESTRATOS.COMPANIA  
    AND USUARIO.USO     = ESTRATOS.USO  
    AND USUARIO.ESTRATO = ESTRATOS.CODIGO  
  INNER JOIN SP_USOS USOS  
     ON ESTRATOS.COMPANIA = USOS.COMPANIA  
    AND ESTRATOS.USO      = USOS.CODIGO  
WHERE USUARIO.COMPANIA   =  s$compania$s
  AND USUARIO.CICLO      BETWEEN  s$cicloInicial$s    AND  s$cicloFinal$s
  AND USUARIO.CODIGORUTA BETWEEN  's$codigoInicial$s'   AND  's$codigoFinal$s'
  AND USUARIO.ESTADO NOT IN ('R')
  AND (    USUARIO.SUBSIDIO    <> CASE WHEN 's$parametro$s' LIKE 'NO' THEN 0 ELSE USUARIO.SUBSIDIO + 1 END
        OR USUARIO.SOBREPRECIO <> CASE WHEN 's$parametro$s' LIKE 'NO' THEN 0 ELSE USUARIO.SOBREPRECIO + 1 END)
GROUP BY ESTRATOS.NOMBRE,  
         USOS.NOMBRE,  
         USUARIO.USO,  
         USUARIO.ESTRATO  
UNION ALL 
