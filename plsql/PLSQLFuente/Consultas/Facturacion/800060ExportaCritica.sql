SELECT 
    SP_USUARIO.CICLO AS "Ciclo",  
    SP_USUARIO.CODIGOINTERNO AS "Interno",
    SP_USUARIO.CODIGORUTA AS "Ruta",
    SP_USUARIO.LECTURA AS "Lectura Actual",
    CASE SP_USUARIO.DESVIACIONAFORO WHEN 0 THEN  'No' ELSE  'Sí' END AS "¿Activa Desviación?",
    SP_USUARIO.LECTURAAFORO AS "Lectura Aforo",
    TO_CHAR(SP_USUARIO.FECHALECTURAAFORO, 'DD/MM/YYYY') AS "Fecha Aforo",
    (SP_USUARIO.LECTURAAFORO - SP_USUARIO.LECTURA) AS "Consumo Aforo",
    SP_USUARIO.TEMP_CONSUMOPROMCAL                 AS "Promedio",
    (
    CASE
      WHEN (SP_USUARIO.LECTURAAFORO - SP_USUARIO.LECTURA) < SP_USUARIO.TEMP_LIMITEINF
      THEN 'INFERIOR'
      ELSE (
        CASE
          WHEN (SP_USUARIO.LECTURAAFORO - SP_USUARIO.LECTURA) > SP_USUARIO.TEMP_LIMITESUP
          THEN 'SUPERIOR'
          ELSE 'NORMAL'
        END)
    END) AS "Crítica",
    SP_USUARIO.AFOROCONSMANUAL AS "Manual",
    SP_USUARIO.CONSAFOROMICRO AS "Micromedición",
    SP_USUARIO.PRIMERAPELLIDO
    || ' '
    || SP_USUARIO.SEGUNDOAPELLIDO
    || ' '
    || SP_USUARIO.NOMBRES AS "Suscriptor",
    SP_USUARIO.DIRTECNICA AS "Dirección",
    SP_USUARIO.MEDIDOR AS "Medidor",
    SP_USUARIO.NUMERODIGITOS AS "Dígitos",
    SP_USOS.NOMBRE AS "Uso",
    SP_ESTRATOS.NOMBRE AS "Estrato",
    SP_USUARIO.LECTURA1 AS "Lectura1",
    SP_USUARIO.LECTURA2 AS "Lectura2",
    SP_USUARIO.LECTURA3 AS "Lectura3",
    SP_USUARIO.LECTURA4 AS "Lectura4",
    SP_USUARIO.LECTURA5 AS "Lectura5",
    SP_USUARIO.LECTURA6 AS "Lectura6",
    SP_USUARIO.CONSUMO1 AS "Consumo1",
    SP_USUARIO.CONSUMO2 AS "Consumo2",
    SP_USUARIO.CONSUMO3 AS "Consumo3",
    SP_USUARIO.CONSUMO4 AS "Consumo4",
    SP_USUARIO.CONSUMO5 AS "Consumo5",
    SP_USUARIO.CONSUMO6 AS "Consumo6",
    TERCERO.NOMBRE AS "Aforador",
    SP_PROBLEMA.NOMBRE AS "Problema",
    SP_USUARIO_PROBLEMA.OBSERVACION AS "Observación",
    SP_USUARIO.METROSDESVIACIONAFORO AS "Metros Desviación",
    CASE SP_USUARIO.ESTADO 
      WHEN 'A' THEN 'Activo'
      WHEN 'C' THEN 'Cortado'
      WHEN 'R' THEN 'Retirado'
      WHEN 'S' THEN 'Suspendido'
      ELSE ''
    END AS "Estado del Usuario",
    SP_USUARIO.CONSUMO AS "Consumo",
    CASE SP_USUARIO.INDDESHABITADO WHEN  0 THEN 'No' ELSE  'Sí' END AS "¿Deshabitado?",
    CASE SP_USUARIO.CHAPETAS WHEN 0 THEN  'No' ELSE  'Sí' END AS "¿Chapetas?"
FROM SP_USUARIO
INNER JOIN SP_USOS
    ON SP_USUARIO.COMPANIA = SP_USOS.COMPANIA
    AND SP_USUARIO.USO     = SP_USOS.CODIGO
INNER JOIN SP_ESTRATOS
    ON (SP_USUARIO.COMPANIA = SP_ESTRATOS.COMPANIA
    AND SP_USUARIO.USO      = SP_ESTRATOS.USO
    AND SP_USUARIO.ESTRATO  = SP_ESTRATOS.CODIGO)
LEFT JOIN SP_AFORADORES
    ON (SP_USUARIO.COMPANIA = SP_AFORADORES.COMPANIA
    AND SP_USUARIO.AFORADOR = SP_AFORADORES.CODIGO)
LEFT JOIN TERCERO
    ON (SP_AFORADORES.COMPANIA = TERCERO.COMPANIA
    AND SP_AFORADORES.NIT      = TERCERO.NIT
    AND SP_AFORADORES.SUCURSAL = TERCERO.SUCURSAL)
LEFT JOIN SP_USUARIO_PROBLEMA
    ON (SP_USUARIO.COMPANIA = SP_USUARIO_PROBLEMA.COMPANIA
    AND SP_USUARIO.CICLO = SP_USUARIO_PROBLEMA.CICLO
    AND SP_USUARIO.CODIGORUTA = SP_USUARIO_PROBLEMA.CODIGORUTA
    AND s$anoSiguiente$s = SP_USUARIO_PROBLEMA.ANO
    AND 's$periodoSiguiente$s' = SP_USUARIO_PROBLEMA.PERIODO)
LEFT JOIN SP_PROBLEMA
    ON (SP_USUARIO_PROBLEMA.COMPANIA = SP_PROBLEMA.COMPANIA
    AND SP_USUARIO_PROBLEMA.CLASE = SP_PROBLEMA.CLASEPROBLEMA
    AND SP_USUARIO_PROBLEMA.PROBLEMA = SP_PROBLEMA.CODIGO)
WHERE SP_USUARIO.COMPANIA = 's$compania$s'
AND SP_USUARIO.CICLO      =s$ciclo$s
AND SP_USUARIO.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
AND SP_USUARIO.ANO     = s$ano$s
AND SP_USUARIO.PERIODO = s$periodo$s 
s$condicion$s
