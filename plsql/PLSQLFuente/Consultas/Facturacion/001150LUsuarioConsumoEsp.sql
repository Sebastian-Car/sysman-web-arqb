SELECT CODIGORUTA,
	CODIGOINTERNO,
	NIT,
	NVL(PRIMERAPELLIDO,' ')||' '||NVL(SEGUNDOAPELLIDO,' ')||' '||NVL(NOMBRES,' ') NOMBRECOMPLETO,
	DIRTECNICA,
	DIRGUIA,
	USO,
  	LECTURA1,
	LECTURA,
	CONSUMOPROM,
	CONSUMO,
	CICLO
FROM SP_USUARIO
WHERE COMPANIA  =s$compania$s
	AND CICLO   =s$ciclo$s
	AND CONSUMO>=s$consumo$s
ORDER BY CODIGORUTA
