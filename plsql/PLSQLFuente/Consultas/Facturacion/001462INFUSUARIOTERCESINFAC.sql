SELECT SP_EMPRESAS_TERCERIZA.NIT,
  SP_EMPRESAS_TERCERIZA.NOMBRE EMPRESAASEOEXT,
  SP_USUARIO.CICLO,
  SP_USUARIO.CODIGORUTA,
  SP_USUARIO.CODIGOINTERNO,
  SP_USUARIO.NOMBRES
  ||' '
  || SP_USUARIO.PRIMERAPELLIDO
  ||' '
  || SP_USUARIO.SEGUNDOAPELLIDO NOMBRE,
  TO_CHAR(SP_USUARIO.FECHAINGASEOEXT, 'DD/MM/YYYY') FECHAINGASEOEXT,
  SP_USUARIO.CODIGO_EXTERNO,
  SP_HISTORIA_EXTERNA.FACTURA
FROM SP_USUARIO
LEFT JOIN SP_HISTORIA_EXTERNA
ON SP_USUARIO.COMPANIA                 = SP_HISTORIA_EXTERNA.COMPANIA
AND SP_USUARIO.CICLO                    = SP_HISTORIA_EXTERNA.CICLO
AND SP_USUARIO.CODIGORUTA               = SP_HISTORIA_EXTERNA.CODIGORUTA
AND SP_USUARIO.PERIODO                  = SP_HISTORIA_EXTERNA.PERIODO
AND SP_USUARIO.ANO                       = SP_HISTORIA_EXTERNA.ANO
INNER JOIN SP_EMPRESAS_TERCERIZA
ON SP_USUARIO.COMPANIA         = SP_EMPRESAS_TERCERIZA.COMPANIA
AND SP_USUARIO.EMPRESAASEOEXT  = SP_EMPRESAS_TERCERIZA.ID
WHERE SP_USUARIO.COMPANIA      = s$compania$s
AND SP_USUARIO.CICLO           = s$ciclo$s
AND SP_HISTORIA_EXTERNA.FACTURA  IS NULL
AND SP_USUARIO.ANO             = s$anio$s
AND SP_USUARIO.PERIODO         = s$periodo$s
s$condicionEmpresa$s
