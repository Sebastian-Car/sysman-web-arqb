SELECT 
  SP_USUARIO.PERIODO,
  SP_USUARIO.SEGUNDOAPELLIDO,
  SP_USUARIO.NOMBRES,
  SP_USUARIO.DIRTECNICA, 
  SP_USUARIO.ASEO, 
  SP_USUARIO.COMPANIA, 
  SP_USUARIO.CICLO, 
  SP_USUARIO.CODIGORUTA, 
  SP_USUARIO.INDDESHABITADO, 
  SP_USUARIO.LECTURA1, 
  SP_USUARIO.LECTURA, 
  SP_USUARIO.PRIMERAPELLIDO, 
  SP_USUARIO.FECHARENTRADANUEVOUSUARIO, 
  SP_USUARIO.ESTADO, 
  CASE WHEN SP_USUARIO.LECTURA - SP_USUARIO.LECTURA1 <> 0 AND SP_USUARIO.INDDESHABITADO = -1 THEN 'Debe Revisar' ELSE '' END AS ALERTA,
  SP_USUARIO.CONSUMO,
  SP_USUARIO.CONSUMO1
FROM SP_USUARIO
WHERE SP_USUARIO.COMPANIA=s$compania$s
  AND SP_USUARIO.ASEO NOT IN (0)
  AND SP_USUARIO.CICLO=s$ciclo$s
  AND SP_USUARIO.INDDESHABITADO NOT IN(0) 
  AND SP_USUARIO.ESTADO NOT IN ('R')
  AND SP_USUARIO.CONSUMO >=s$consumo$s
ORDER BY SP_USUARIO.COMPANIA, SP_USUARIO.CICLO, SP_USUARIO.CODIGORUTA
