 SELECT   FACTURADO.COMPANIA        
 , FACTURADO.CICLO        
 , FACTURADO.CODIGORUTA        
 , FACTURADO.ANO        
 , FACTURADO.PERIODO        
 , FACTURADO.CONCEPTO        
 , CONCEPTOS.NOMBRE        
 , FACTURADO.VALOR_FACTURADO  VALOR      
 , CONCEPTOS.CODIGO        
 , FINANCIABLES.VALORCUOTA        
 , FINANCIABLES.NROCUOTA        
 , NUMEROCUOTAS-NROCUOTA FALTA       
 , FINANCIABLES.SALDOFINANCIABLE 
FROM SP_FACTURADO FACTURADO     
	INNER JOIN SP_CONCEPTOS CONCEPTOS        
		ON  FACTURADO.COMPANIA = CONCEPTOS.COMPANIA      
		AND FACTURADO.CONCEPTO = CONCEPTOS.CODIGO      
	LEFT JOIN SP_FINANCIABLES FINANCIABLES        
		ON  FACTURADO.COMPANIA = FINANCIABLES.COMPANIA         
		AND FACTURADO.CICLO = FINANCIABLES.CICLO         
		AND FACTURADO.CODIGORUTA = FINANCIABLES.CODIGORUTA       
		AND FACTURADO.ANO = FINANCIABLES.ANO         
		AND FACTURADO.PERIODO = FINANCIABLES.PERIODO   
		AND FACTURADO.CONCEPTO = FINANCIABLES.CONCEPTO 
WHERE FACTURADO.COMPANIA='001'   
AND ((FACTURADO.CONCEPTO NOT IN (1,2,3,4,5,20,21,22,33,46,48)  		 
AND FACTURADO.CONCEPTO<=50)       
OR (FACTURADO.CONCEPTO IN (246,247,248,249)))  
AND FACTURADO.VALOR_FACTURADO NOT IN (0)  
AND FACTURADO.COMPANIA = $P{PR_COMPANIA}   
AND FACTURADO.ANO = $P{PR_ANO}   
 AND FACTURADO.CICLO = $P{PR_CICLO}   
 AND FACTURADO.PERIODO = $P{PR_PERIODO}   
 AND FACTURADO.CODIGORUTA = $P{PR_CODIGORUTA}
