WITH DISCRIMINADO_CONCEPTO_RECAUDO AS(
  SELECT   SP_D_RECAUDO.CONCEPTO
          ,SP_D_RECAUDO.COMPANIA
          ,SP_D_RECAUDO.VALORPAGOPERIODO
          ,SP_D_RECAUDO.CREDITOABONADO
          ,SUM(
             CASE
               WHEN SP_D_RECAUDO.CONCEPTO=2
               THEN SP_USUARIO.VALCONSUMO
               ELSE (
                 CASE
                   WHEN SP_D_RECAUDO.CONCEPTO=5
                   THEN SP_USUARIO.VALCONSUMOPROM
                 END)
             END) VALCONSUM
          ,SUM(
             CASE
               WHEN SP_D_RECAUDO.CONCEPTO=2
               THEN SP_USUARIO.VALCONSUMO
               ELSE (
                 CASE
                   WHEN SP_D_RECAUDO.CONCEPTO=5
                   THEN 0
                   ELSE 0
                 END)
             END) BASICO
          ,SUM(
             CASE
               WHEN SP_D_RECAUDO.CONCEPTO=2
               THEN SP_USUARIO.VALORCOMPLEMENTARIO
               ELSE (
                 CASE
                   WHEN SP_D_RECAUDO.CONCEPTO=5
                   THEN 0
                   ELSE 0
                 END)
             END) COMPL
          ,SUM(
             CASE
               WHEN SP_D_RECAUDO.CONCEPTO=2
               THEN SP_USUARIO.VALORSUNTUARIO
               ELSE (
                 CASE
                   WHEN SP_D_RECAUDO.CONCEPTO=5
                   THEN 0
                   ELSE 0
                 END)
             END) SUNT
  FROM     SP_USUARIO
    INNER JOIN SP_D_RECAUDO
       ON      SP_USUARIO.COMPANIA              = SP_D_RECAUDO.COMPANIA
      AND      SP_USUARIO.PAQUETEPAGOPERPROCESO = SP_D_RECAUDO.NUMEROPAQUETE
      AND      SP_USUARIO.BANCOPERPROCESO       = SP_D_RECAUDO.BANCO
      AND      SP_USUARIO.FECHAPAGOPERPROCESO   = SP_D_RECAUDO.FECHA
  WHERE    SP_D_RECAUDO.COMPANIA = s$compania$s
    AND    SP_D_RECAUDO.CONCEPTO IN (2,5)
    AND    SP_D_RECAUDO.FECHA    IS NOT NULL
    AND    SP_D_RECAUDO.FECHA    BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  GROUP BY SP_D_RECAUDO.CONCEPTO
          ,SP_D_RECAUDO.COMPANIA
          ,SP_D_RECAUDO.VALORPAGOPERIODO
          ,SP_D_RECAUDO.CREDITOABONADO
)
SELECT   DISCRIMINADO_CONCEPTO_RECAUDO.CONCEPTO
        ,SUM(DISCRIMINADO_CONCEPTO_RECAUDO.VALORPAGOPERIODO) PAGOPERIODO
        ,SUM(DISCRIMINADO_CONCEPTO_RECAUDO.CREDITOABONADO) VALCREDITOABONADO
        ,SUM(ROUND(DISCRIMINADO_CONCEPTO_RECAUDO.VALCONSUM,0)) VALCONSUMO
        ,SUM(ROUND(DISCRIMINADO_CONCEPTO_RECAUDO.BASICO,0)) VALBASICO
        ,SUM(ROUND(DISCRIMINADO_CONCEPTO_RECAUDO.COMPL,0)) VALCOMPLEMENTARIO
        ,SUM(ROUND(DISCRIMINADO_CONCEPTO_RECAUDO.SUNT,0)) VALSUNTUARIO
FROM     DISCRIMINADO_CONCEPTO_RECAUDO
GROUP BY DISCRIMINADO_CONCEPTO_RECAUDO.CONCEPTO
        ,DISCRIMINADO_CONCEPTO_RECAUDO.COMPANIA
