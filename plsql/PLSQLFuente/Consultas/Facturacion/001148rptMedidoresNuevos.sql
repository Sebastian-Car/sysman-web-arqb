SELECT (MEDIDOR.ANO || MEDIDOR.PERIODO) AS NOMBREDEPERIODO,   
  (PRIMERAPELLIDO || ' '  || SEGUNDOAPELLIDO || ' '  || NOMBRES) AS NOMBRECOMPLETO,  
PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(MEDIDOR.COMPANIA,MEDIDOR.ANO,MEDIDOR.PERIODO,NULL) AS  NOMPERIODO,  
  MEDIDOR.LECTURAINICIAL,  
  MEDIDOR.LECTMEDANT,  
  MEDIDOR.LECTACTUAL,  
  USUARIO.COMPANIA,   
  USUARIO.CICLO,   
  USUARIO.CODIGORUTA,  
  (PRIMERAPELLIDO || ' ' || SEGUNDOAPELLIDO || ' ' || NOMBRES) AS NOMBREUSUARIO  
FROM SP_MEDIDOR MEDIDOR   
LEFT JOIN SP_USUARIO USUARIO   
   ON MEDIDOR.CODIGORUTA = USUARIO.CODIGORUTA   
  AND MEDIDOR.CICLO = USUARIO.CICLO   
  AND MEDIDOR.COMPANIA = USUARIO.COMPANIA  
WHERE MEDIDOR.COMPANIA='s$compania$s'  
   AND  (MEDIDOR.ANO || MEDIDOR.PERIODO)   
 BETWEEN  s$periodoInicialPer$s  
AND  s$periodoFinalPer$s   
   AND USUARIO.CODIGORUTA BETWEEN 's$cicloInicial$s' AND 's$cicloFinal$s'
