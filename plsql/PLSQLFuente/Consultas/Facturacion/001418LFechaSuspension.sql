SELECT 
   U.CODIGORUTA
  ,U.CODIGOINTERNO
  ,NVL(U.PRIMERAPELLIDO,' ') ||' '||NVL(U.SEGUNDOAPELLIDO,' ')||' '||NVL(U.NOMBRES,' ') NOMBREUSUARIO
  ,U.DIRTECNICA
  ,U.MEDIDOR
  ,U.FECHAACTASUSPEN
FROM SP_USUARIO U
WHERE U.COMPANIA         =s$compania$s
  AND U.CICLO            =s$ciclo$s
  AND U.CODIGORUTA BETWEEN s$codigoInicial$s AND s$codigoFinal$s
  AND U.FECHAACTASUSPEN <=s$fecha$s
  AND U.FECHAPAGOPERPROCESO IS NULL
