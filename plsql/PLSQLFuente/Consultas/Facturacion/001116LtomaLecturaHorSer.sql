SELECT U.CODIGORUTA,
 	DIRTECNICA,
  	SUBSTR(U.CODIGORUTA,1,2) AS RUTA,
   	U.CICLO,
    NVL(PRIMERAPELLIDO,' ') ||' '||NVL(SEGUNDOAPELLIDO,' ')||' '|| NVL(NOMBRES,' ') AS NOMBRECOMPLETO,
    CODIGOINTERNO,
    M.CODIGO MEDIDOR, 
    USO,
	ESTRATO,
    CASE ALCANTARILLADO
    	WHEN 0 
    		THEN ' '
    	ELSE 'X'
    END ALCANTARILLADO,
    CASE ASEO
    	WHEN 0 
    		THEN ' '
    	ELSE 'X'
    END ASEO,
    CASE ACUEDUCTO
    	WHEN 0 
    		THEN ' '
    	ELSE 'X'
    END ACUEDUCTO
FROM SP_USUARIO U
	INNER JOIN SP_MEDIDOR M 
		ON U.COMPANIA 	 = M.COMPANIA
		AND U.CODIGORUTA = M.CODIGORUTA
		AND U.CICLO 	 = M.CICLO
WHERE U.ESTADO NOT IN ('R')
	AND U.COMPANIA =s$compania$s
  	AND U.CICLO    =s$ciclo$s
  	AND U.CODIGORUTA BETWEEN s$codInicial$s AND s$codFinal$s
ORDER BY U.CODIGORUTA
