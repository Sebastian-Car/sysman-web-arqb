WITH SP_DEUDAUSUARIO AS (
    SELECT SP_FACTURADO.COMPANIA,
        SP_FACTURADO.CODIGORUTA,
        SP_FACTURADO.ANO,
        SP_FACTURADO.PERIODO,
        SP_FACTURADO.VALOR_FACTURADO AS Deuda
    FROM SP_FACTURADO
    WHERE SP_FACTURADO.COMPANIA = s$compania$s
        AND SP_FACTURADO.CONCEPTO = 250
)
s$withSinAbonos$s
SELECT SP_USUARIO.CODIGORUTA,
    REPLACE(NVL(SP_USUARIO.PRIMERAPELLIDO, ' ') || ' ' ||
    NVL(SP_USUARIO.SEGUNDOAPELLIDO, ' ')|| ' ' ||
    NVL(SP_USUARIO.NOMBRES, ' '), '  ', ' ') NOMBRE,
    SP_USUARIO.MEDIDOR,
    SP_USUARIO.LECTURA1,
    SP_USUARIO.PERIODOSATRASO,
    SP_USUARIO.LECTURA,
    NVL(SP_USUARIO.BANCOPERPROCESO,0) AS PAGO,
    SP_USUARIO.DIRTECNICA,
    SP_DEUDAUSUARIO.DEUDA,
    SP_USOS.NOMBRE     AS NOMBREUSO,
    SP_ESTRATOS.NOMBRE AS NOMBREESTRATO
FROM SP_USUARIO 
    INNER JOIN SP_DEUDAUSUARIO
        ON SP_USUARIO.COMPANIA = SP_DEUDAUSUARIO.COMPANIA
            AND SP_USUARIO.ANO = SP_DEUDAUSUARIO.ANO
            AND SP_USUARIO.PERIODO   = SP_DEUDAUSUARIO.PERIODO
            AND SP_USUARIO.CODIGORUTA = SP_DEUDAUSUARIO.CODIGORUTA
    INNER JOIN SP_USOS
        ON SP_USUARIO.COMPANIA = SP_USOS.COMPANIA
            AND SP_USUARIO.USO = SP_USOS.CODIGO
    INNER JOIN SP_ESTRATOS
        ON SP_USUARIO.COMPANIA = SP_ESTRATOS.COMPANIA
            AND SP_USUARIO.USO = SP_ESTRATOS.USO
            AND SP_USUARIO.ESTRATO = SP_ESTRATOS.CODIGO
    s$joinAdicional$s
WHERE SP_USUARIO.COMPANIA = s$compania$s
    AND SP_USUARIO.CICLO = s$ciclo$s
    AND SP_USUARIO.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
    AND NVL(SP_USUARIO.BANCOPERPROCESO, 0) = 0
    AND SP_USUARIO.PERIODOSATRASO BETWEEN s$atrasoInferior$s AND s$atrasoSuperior$s 
    AND SP_DEUDAUSUARIO.DEUDA BETWEEN s$deudaInicial$s AND s$deudaFinal$s
    s$condicionAdicional$s
