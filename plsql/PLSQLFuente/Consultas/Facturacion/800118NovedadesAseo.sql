SELECT H.ANO,
  H.PERIODO,
  H.CODIGO,
  E.NIT,
  E.NOMBRE       AS EMPRESA,
 TO_CHAR( H.FECHA_INICIO,'DD/MM/YYYY') AS F_INICIO,
TO_CHAR(  H.FECHA_FIN,'DD/MM/YYYY')    AS F_FIN,
 TO_CHAR( H.FECHA_GENERA,'DD/MM/YYYY') AS F_GENERA,
  H.TARIFA,
  H.ESTRATO,
  H.CATEGORIA AS CATEGORI,
  H.VALORDEU,
  H.MESESMORA AS MESESMOR,
  H.INTMORA,
  H.FRECUENCIA_ASEO        AS FRECUENC,
  H.FRECUENCIA_BARRIDO     AS FRECBARR,
  H.VALORASEO_SINSUBOSOBRE AS ASEO,
  H.SUBOSOBRE              AS SUB_CONT,
  H.SUBOSOBRE_DES          AS TXT_S_C,
  H.VALORASEO              AS TOTALASE,
  H.HISTORICO1             AS HISTOR01,
  H.HISTORICO2             AS HISTOR02,
  H.HISTORICO3             AS HISTOR03,
  H.HISTORICO4             AS HISTOR04,
  H.HISTORICO5             AS HISTOR05,
  H.HISTORICO6             AS HISTOR06,
  H.VALOR_FINAN            AS VRFINANC,
  H.CUOTA_FINAN            AS CUOTAFIN,
  H.UNIDADES_RESID         AS UNIDADES,
  NVL(H.TDI_ASEO,0)        AS TDI,
  H.BARRIDO_TBL            AS TBL,
  H.RECOLECCION_TRT        AS TRT,
  H.TRAMOEXCE_TTE          AS TTE,
  H.DISPOFINAL_TDT         AS TDT,
  H.MANEJOREC_TFR          AS TFR,
  H.REF01,
  H.TEX01,
  H.REF02,
  H.TEX02,
  H.REF03,
  H.TEX03,
  H.REF04,
  H.TEX04,
  H.REF05,
  H.TEX05,
  H.VALOR_COB,
  H.COMISION,
  H.FACTURA AS NFACTURA,
  TO_CHAR( H.FECHA_PAGO,'DD/MM/YYYY') FECHA_PAGO
FROM SP_HISTORIA_EXTERNA H
INNER JOIN SP_EMPRESAS_TERCERIZA E
ON (H.COMPANIA    = E.COMPANIA)
AND (H.ID_EMPRESA = E.ID)
WHERE H.COMPANIA  =s$compania$s
AND H.ID_EMPRESA  = s$empresa$s
AND H.CICLO       = s$ciclo$s
AND H.ANO         = s$ano$s
AND H.PERIODO     = 's$periodo$s'
AND H.FECHA_PAGO BETWEEN
  CASE
    WHEN s$fechaInicial$s IS NULL
    THEN H.FECHA_PAGO

    ELSE s$fechaInicial$s
  END
AND
  CASE
    WHEN s$fechaFinal$s IS NULL
    THEN H.FECHA_PAGO

    ELSE s$fechaFinal$s
  END
