SELECT SP_USUARIO.CODIGORUTA,  
  PRIMERAPELLIDO || ' ' || SEGUNDOAPELLIDO || ' '  || NOMBRES AS NOMBRECOMPLETO,  
  SP_CONCEPTOS.CODIGO,   
  SP_CONCEPTOS.NOMBRE AS NOMBRECONCEPTO,  
  SP_FACTURADO.VALOR_FACTURADOANT,  
  SP_FACTURADO.VALOR_FACTURADOIN,  
  SP_FACTURADO.VALOR_FACTURADO,  
  SP_FACTURADO.DEUDAANT,  
  (SP_FACTURADO.VALOR_FACTURADOANT+SP_FACTURADO.DEUDAANT) AS TOTALANTERIOR, 
  SP_FACTURADO.DEUDA, 
  PCK_SYSMAN_UTL.FC_ROUND((SP_FACTURADO.VALOR_FACTURADO-SP_FACTURADO.VALOR_FACTURADOIN),2) AS DIFERENCIA, 
  NVL(BANCOPERANTERIOR,'') AS EXPR1, 
  SP_USUARIO.COMPANIA, 
  (LPAD(SP_FACTURADO.ANO,4,'0') || LPAD(SP_FACTURADO.PERIODO,2,'0')) AS PERIODOUNICO 
FROM SP_USUARIO 
LEFT JOIN (SP_FACTURADO 
  LEFT JOIN SP_CONCEPTOS 
    ON  SP_FACTURADO.COMPANIA = SP_CONCEPTOS.COMPANIA 
    AND SP_FACTURADO.CONCEPTO = SP_CONCEPTOS.CODIGO) 
  ON  SP_USUARIO.COMPANIA = SP_FACTURADO.COMPANIA 
  AND SP_USUARIO.CICLO = SP_FACTURADO.CICLO  
  AND SP_USUARIO.ANO  = SP_FACTURADO.ANO 
  AND SP_USUARIO.PERIODO  = SP_FACTURADO.PERIODO 
  AND SP_USUARIO.CODIGORUTA = SP_FACTURADO.CODIGORUTA 
WHERE SP_USUARIO.COMPANIA = 's$compania$s' 
  s$concicionCiclo$s  
  AND SP_FACTURADO.VALOR_FACTURADO <> SP_FACTURADO.VALOR_FACTURADOIN  
  AND (LPAD(SP_FACTURADO.ANO,4,'0') || LPAD(SP_FACTURADO.PERIODO,2,'0'))   
  BETWEEN (LPAD('s$anoInicial$s',4,'0') ||  LPAD('s$periodoInicial$s',2,'0'))  AND (LPAD('s$anoFinal$s',4,'0') ||  LPAD('s$periodoFinal$s',2,'0'))  
  AND (SP_FACTURADO.CONCEPTO BETWEEN 1 AND 49   
  OR SP_FACTURADO.CONCEPTO IN (201,202,203,204,205,206,207,246,247,248,249))
