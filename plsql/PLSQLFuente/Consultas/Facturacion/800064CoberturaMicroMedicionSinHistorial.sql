SELECT SP_USUARIO.COMPANIA,
  SP_USUARIO.ANO,
  SP_USUARIO.PERIODO,
  SP_USUARIO.USO,
  SP_USUARIO.ESTRATO,
  COUNT(SP_USUARIO.ACUEDUCTO) AS ACTIVOS,
  SUM(CASE WHEN NVL(SP_USUARIO.MEDIDOR,0) <>'0'
           THEN 1
           ELSE 0
           END) AS CONMEDIDOR,
  SUM(CASE WHEN NVL(SP_USUARIO.MEDIDOR,0) ='0'
           THEN 1
           ELSE 0
            END) AS SINMEDIDOR
  s$complementoUsuario$s
FROM SP_USUARIO
WHERE SP_USUARIO.COMPANIA=s$compania$s
s$variableCiclo$s 
s$variableHistoico$s
AND SP_USUARIO.ACUEDUCTO = -1
GROUP BY SP_USUARIO.COMPANIA,
  SP_USUARIO.ANO,
  SP_USUARIO.PERIODO,
  SP_USUARIO.USO,
  SP_USUARIO.ESTRATO 
  s$complementoUsuario$s
