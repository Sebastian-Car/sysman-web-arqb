 SELECT SP_USUARIO.CODIGORUTA,
       SP_USUARIO.CODIGOINTERNO,
       SP_USUARIO.PRIMERAPELLIDO||' '||SP_USUARIO.SEGUNDOAPELLIDO||' '||SP_USUARIO.NOMBRES NOMBRES,
       SP_USUARIO.DIRTECNICA,
       SP_USOS.NOMBRE USO,
       SP_ESTRATOS.NOMBRE ESTRATO,
       DECODE(SP_USUARIO.ESTADO,'A','Activo'
                               ,'S','Suspendido'
                               ,'C','Cortado'
                               ,'R','Retirado') ESTADO,
       SP_USUARIO.LECTURA,
       SP_USUARIO.LECTURA1,
       SP_USUARIO.LECTURA2, 
       SP_USUARIO.CONSUMOPROM
FROM SP_USUARIO 
    LEFT JOIN SP_ESTRATOS
        ON  SP_USUARIO.COMPANIA = SP_ESTRATOS.COMPANIA
        AND SP_USUARIO.USO = SP_ESTRATOS.USO
        AND SP_USUARIO.ESTRATO = SP_ESTRATOS.CODIGO
    LEFT JOIN SP_USOS
        ON  SP_USUARIO.COMPANIA = SP_USOS.COMPANIA
        AND SP_USUARIO.USO = SP_USOS.CODIGO
  
WHERE SP_USUARIO.COMPANIA = s$compania$s
  AND SP_USUARIO.CICLO    = s$ciclo$s 
  AND CASE WHEN SP_USUARIO.FIMM NOT IN ('P','F')
           THEN 'S'
           ELSE SP_USUARIO.FIMM
      END = s$estado$s   ORDER BY SP_USUARIO.COMPANIA,   
  SP_USUARIO.CICLO, 
  SP_USUARIO.CODIGORUTA
