SELECT 
  PAGO.COMPANIA, 
  PAGO.CODIGORUTA,   
  PAGO.USUARIO,
  PAGO.VALOR VALORPAGO,
  PRIMERAPELLIDO||' '||SEGUNDOAPELLIDO||' '||NOMBRES AS NOMBRECOMPLETO, 
  USUARIO.CODIGOINTERNO,
  HISTORIA_EXTERNA.VALORASEO, 
  HISTORIA_CONVENIOS.TOTAL,
  NVL(PAGO.VALOR,0)+NVL(HISTORIA_EXTERNA.VALORASEO,0)+NVL(HISTORIA_CONVENIOS.TOTAL,0) TOTH, 
  'Primer Pago' OPERACION 
FROM SP_ABONOS PAGO 
  LEFT JOIN SP_USUARIO USUARIO
    ON  PAGO.COMPANIA   = USUARIO.COMPANIA 
    AND PAGO.CICLO      = USUARIO.CICLO 
    AND PAGO.CODIGORUTA = USUARIO.CODIGORUTA
  LEFT JOIN SP_HISTORIA_EXTERNA HISTORIA_EXTERNA 
    ON  PAGO.COMPANIA      = HISTORIA_EXTERNA.COMPANIA
    AND PAGO.BANCO         = HISTORIA_EXTERNA.BANCO_PAGO
    AND PAGO.FECHA         = HISTORIA_EXTERNA.FECHA_PAGO
    AND PAGO.CODIGOINTERNO = HISTORIA_EXTERNA.CODIGO
  LEFT JOIN SP_HISTORIA_CONVENIOS HISTORIA_CONVENIOS
    ON  PAGO.COMPANIA      = HISTORIA_CONVENIOS.COMPANIA 
    AND PAGO.BANCO         = HISTORIA_CONVENIOS.BANCO_PAGO
    AND PAGO.FECHA         = HISTORIA_CONVENIOS.FECHA_PAGO 
    AND PAGO.CODIGOINTERNO = HISTORIA_CONVENIOS.CODIGO 
WHERE PAGO.COMPANIA     = s$compania$s 
  AND PAGO.BANCO              = 's$banco$s'
  AND TRUNC(PAGO.FECHA) = TO_DATE('s$fecha$s','DD/MM/YYYY') 
  s$condCiclo$s   
  AND s$condConsulta$s NOT IN (0)     
UNION ALL 
 SELECT 
  PAGO.COMPANIA,
  PAGO.CODIGORUTA,
  PAGO.USUARIO, 
  PAGO.VALORPAGO,  
  PRIMERAPELLIDO||' '||SEGUNDOAPELLIDO||' '||NOMBRES NOMBRECOMPLETO,
  PAGO.CODIGOINTERNO,
  HISTORIA_EXTERNA.VALORASEO,
  HISTORIA_CONVENIOS.TOTAL,
  NVL(PAGO.VALORPAGO, 0) + NVL(HISTORIA_EXTERNA.VALORASEO, 0) + NVL(HISTORIA_CONVENIOS.TOTAL, 0) TOTH,
  CASE PAGO.OPERACION   WHEN 'X' THEN 'Borrar Pago'
                        WHEN '1' THEN 'Primer Pago'
                        WHEN '2' THEN 'Segundo Pago'
                        WHEN 'D' THEN 'Pago Doble'
  END OPERACION   
FROM SP_PAGO PAGO 
  LEFT JOIN SP_USUARIO USUARIO
    ON  PAGO.COMPANIA   = USUARIO.COMPANIA
    AND PAGO.CICLO      = USUARIO.CICLO
    AND PAGO.CODIGORUTA = USUARIO.CODIGORUTA 
  INNER JOIN SP_HISTORIA_EXTERNA HISTORIA_EXTERNA 
    ON   PAGO.COMPANIA             = HISTORIA_EXTERNA.COMPANIA 
    AND PAGO.CODIGOINTERNO  = HISTORIA_EXTERNA.CODIGO 
    AND PAGO.FECHA                    = HISTORIA_EXTERNA.FECHA_PAGO 
    AND PAGO.BANCO                   = HISTORIA_EXTERNA.BANCO_PAGO 
    AND PAGO.NUMEROPAQUETE = HISTORIA_EXTERNA.PAQUETE_PAGO  
  INNER JOIN SP_HISTORIA_CONVENIOS HISTORIA_CONVENIOS 
    ON   PAGO.COMPANIA      = HISTORIA_CONVENIOS.COMPANIA 
    AND PAGO.BANCO         = HISTORIA_CONVENIOS.BANCO_PAGO 
    AND PAGO.FECHA         = HISTORIA_CONVENIOS.FECHA_PAGO 
    AND PAGO.CODIGOINTERNO = HISTORIA_CONVENIOS.CODIGO 
    AND PAGO.NUMEROPAQUETE = HISTORIA_CONVENIOS.PAQUETE_PAGO 
WHERE PAGO.COMPANIA      = s$compania$s 
  AND PAGO.BANCO         = 's$banco$s'
  AND PAGO.NUMEROPAQUETE = 's$paquete$s'
  AND TRUNC(PAGO.FECHA)  = TO_DATE('s$fecha$s','DD/MM/YYYY')
  s$condCiclo$s
  AND s$condConsulta$s  IN (0)     
