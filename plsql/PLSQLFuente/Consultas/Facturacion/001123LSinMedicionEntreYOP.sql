            AND SP_AFORADORES.NIT       = TERCERO.NIT
            AND SP_AFORADORES.SUCURSAL  = TERCERO.SUCURSAL)
INNER JOIN (SELECT  SP_PROBLEMA.NOMBRE AS PROBAFORO,
        SP_PROBLEMA.CODIGO,
        SP_USUARIO_PROBLEMA.COMPANIA,
        SP_USUARIO_PROBLEMA.LECTURA,
        SP_USUARIO_PROBLEMA.ANO,
        SP_USUARIO_PROBLEMA.PERIODO,
        SP_USUARIO_PROBLEMA.CICLO,
        SP_USUARIO_PROBLEMA.CODIGORUTA
FROM SP_PROBLEMA
  INNER JOIN SP_USUARIO_PROBLEMA
    ON  (SP_PROBLEMA.CODIGO        = SP_USUARIO_PROBLEMA.PROBLEMA
    AND SP_PROBLEMA.CLASEPROBLEMA  = SP_USUARIO_PROBLEMA.CLASE
    AND SP_PROBLEMA.COMPANIA       = SP_USUARIO_PROBLEMA.COMPANIA)) SP_PROBLEMAAFORO
ON SP_HISTORIA.PERIODO       = SP_PROBLEMAAFORO.PERIODO
AND SP_HISTORIA.ANO          = SP_PROBLEMAAFORO.ANO
AND SP_HISTORIA.CODIGORUTA   = SP_PROBLEMAAFORO.CODIGORUTA
AND SP_HISTORIA.CICLO        = SP_PROBLEMAAFORO.CICLO
AND SP_HISTORIA.COMPANIA     = SP_PROBLEMAAFORO.COMPANIA)
ON (SP_USUARIO.CODIGORUTA    = SP_HISTORIA.CODIGORUTA
AND SP_USUARIO.CICLO         = SP_HISTORIA.CICLO
AND SP_USUARIO.COMPANIA      = SP_HISTORIA.COMPANIA)
WHERE SP_HISTORIA.COMPANIA = s$compania$s
  AND SP_HISTORIA.CICLO    BETWEEN s$cicloInicial$s AND s$cicloFinal$s
  AND SP_HISTORIA.ANO      = s$ano$s
  AND SP_HISTORIA.PERIODO  = 's$periodo$s'
  AND SP_PROBLEMAAFORO.CODIGO BETWEEN 's$problemaInicial$s' AND 's$problemaFinal$s'
  AND (CASE
