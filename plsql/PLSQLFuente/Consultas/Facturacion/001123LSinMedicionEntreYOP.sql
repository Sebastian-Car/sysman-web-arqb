WITH SP_PROBLEMAAFORO AS (SELECT  SP_PROBLEMA.NOMBRE AS PROBAFORO,
        SP_PROBLEMA.CODIGO,
        SP_USUARIO_PROBLEMA.COMPANIA,
        SP_USUARIO_PROBLEMA.LECTURA,
        SP_USUARIO_PROBLEMA.ANO,
        SP_USUARIO_PROBLEMA.PERIODO,
        SP_USUARIO_PROBLEMA.CICLO,
        SP_USUARIO_PROBLEMA.CODIGORUTA
FROM SP_PROBLEMA
  INNER JOIN SP_USUARIO_PROBLEMA
    ON  (SP_PROBLEMA.CODIGO         = SP_USUARIO_PROBLEMA.PROBLEMA
    AND SP_PROBLEMA.CLASEPROBLEMA  = SP_USUARIO_PROBLEMA.CLASE
    AND SP_PROBLEMA.COMPANIA       = SP_USUARIO_PROBLEMA.COMPANIA))
SELECT SP_USUARIO.COMPANIA,
        SP_USUARIO.CICLO,
        SP_USUARIO.CODIGORUTA,
        SP_USUARIO.ANO,
        SP_USUARIO.PERIODO,
        SP_USUARIO.CODIGOINTERNO,
        SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO || ' ' || SP_USUARIO.NOMBRES AS NOMBRE,
        SP_USUARIO.DIRTECNICA,
        SP_USUARIO.USO,
        SP_USUARIO.LECTURA,
        SP_USUARIO.LECTURA1,
        SP_USUARIO.LECTURAAFORO,
        SP_USUARIO.CONSUMO,
        SP_USUARIO.CONSUMOPROM,
        (SP_USUARIO.LECTURA - SP_USUARIO.LECTURA1) AS TOTALCONSUMO,
        (CASE
    WHEN SP_USUARIO.LECTURAAFORO=0 AND SP_USUARIO.LECTURA=0
    THEN
      'SIN LECTURA'
    ELSE
      CASE WHEN (SP_USUARIO.LECTURA - SP_USUARIO.LECTURA1) < 0
      THEN
        'MEDIDOR INVERTIDO'
      ELSE
        CASE WHEN (SP_USUARIO.LECTURA - SP_USUARIO.LECTURA1)=0
          THEN
            'MEDIDOR PARADO'
          ELSE
            CASE WHEN (SP_USUARIO.LECTURA - SP_USUARIO.LECTURA1)>10000
            THEN
              'CONSUMO ELEVADO'
            ELSE
              'NORMAL'
            END
        END
      END
   END) AS OBSERVACIONES,
         SP_MEDIDOR.CODIGO  MEDIDOR,
        SP_USUARIO.AFORADOR,
        (TERCERO.NOMBRE1 || ' ' || TERCERO.NOMBRE2 || ' ' || TERCERO.APELLIDO1 || ' ' || TERCERO.APELLIDO2) AS NOMBREAFORADOR,
        SP_PROBLEMAAFORO.PROBAFORO,
        SP_PROBLEMAAFORO.LECTURA AS LSITIO,
        SP_PROBLEMAAFORO.CODIGO
FROM  SP_USUARIO
  LEFT JOIN SP_MEDIDOR ON SP_USUARIO.COMPANIA = SP_MEDIDOR.COMPANIA
  AND SP_USUARIO.CICLO = SP_MEDIDOR.CICLO
  AND SP_USUARIO.CODIGORUTA = SP_MEDIDOR.CODIGORUTA
  AND SP_USUARIO.MEDIDOR = SP_MEDIDOR.CONSECUTIVO
        INNER JOIN SP_AFORADORES
          ON  (SP_USUARIO.AFORADOR  = SP_AFORADORES.CODIGO
         AND   SP_USUARIO.COMPANIA  = SP_AFORADORES.COMPANIA)
          INNER JOIN TERCERO
            ON  (SP_AFORADORES.COMPANIA  = TERCERO.COMPANIA
            AND  SP_AFORADORES.NIT       = TERCERO.NIT
            AND  SP_AFORADORES.SUCURSAL  = TERCERO.SUCURSAL)
            INNER JOIN SP_PROBLEMAAFORO
              ON (SP_USUARIO.COMPANIA    = SP_PROBLEMAAFORO.COMPANIA
             AND SP_USUARIO.CICLO        = SP_PROBLEMAAFORO.CICLO
             AND SP_USUARIO.CODIGORUTA   = SP_PROBLEMAAFORO.CODIGORUTA
             AND SP_USUARIO.ANO          = SP_PROBLEMAAFORO.ANO
             AND SP_USUARIO.PERIODO      = SP_PROBLEMAAFORO.PERIODO)
WHERE SP_USUARIO.COMPANIA = s$compania$s
  AND SP_USUARIO.CICLO    BETWEEN s$cicloInicial$s AND s$cicloFinal$s
  AND SP_USUARIO.ANO      = s$ano$s
  AND SP_USUARIO.PERIODO  = 's$periodo$s'
  AND SP_PROBLEMAAFORO.CODIGO BETWEEN 's$problemaInicial$s' AND 's$problemaFinal$s'
  AND (CASE
    WHEN SP_USUARIO.LECTURAAFORO=0 AND SP_USUARIO.LECTURA=0
    THEN
      'SIN LECTURA'
    ELSE
      CASE WHEN (SP_USUARIO.LECTURA - SP_USUARIO.LECTURA1) < 0
      THEN
        'MEDIDOR INVERTIDO'
      ELSE
        CASE WHEN (SP_USUARIO.LECTURA - SP_USUARIO.LECTURA1)=0
          THEN
            'MEDIDOR PARADO'
          ELSE
            CASE WHEN (SP_USUARIO.LECTURA - SP_USUARIO.LECTURA1)>10000
            THEN
              'CONSUMO ELEVADO'
            ELSE
              'NORMAL'
            END
        END
      END
   END)  s$filtroObservaciones$s
  AND SP_USUARIO.ESTADO NOT IN ('R')
UNION
