SELECT SUBSTR(U.CODIGORUTA,1,3) ||'-'|| SUBSTR(U.CODIGORUTA,4,4) ||'-'|| SUBSTR(U.CODIGORUTA,8,4) ||'-'|| SUBSTR(U.CODIGORUTA,12) CODIGORUTA,
  	DIRTECNICA, 
  	CASE WHEN s$primerDigito$s NOT IN(0)
	  	THEN SUBSTR(U.CODIGORUTA, 1,1)
  		ELSE SUBSTR(U.CODIGORUTA, 1,2)
  	END RUTA,
	U.CICLO,
 	NVL(PRIMERAPELLIDO,' ') ||' '||NVL(SEGUNDOAPELLIDO,' ')||' '|| NVL(NOMBRES,' ') AS NOMBRECOMPLETO,
  	CODIGOINTERNO, 
	U.ESTADO,
    LECTURA,
    M.CODIGO MEDIDOR
FROM SP_USUARIO U
	INNER JOIN SP_MEDIDOR M 
		ON U.COMPANIA 	 = M.COMPANIA
		AND U.CODIGORUTA = M.CODIGORUTA
		AND U.CICLO 	 = M.CICLO
WHERE U.ESTADO NOT IN ('R')
 	AND U.COMPANIA=s$compania$s
  	AND U.CICLO   =s$ciclo$s
    AND U.CODIGORUTA BETWEEN s$codInicial$s AND s$codFinal$s
ORDER BY U.CODIGORUTA
