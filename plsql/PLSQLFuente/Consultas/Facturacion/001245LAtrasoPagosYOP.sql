SELECT  SP_USUARIO.COMPANIA, 
        SP_USUARIO.CICLO, 
        SP_USUARIO.CODIGORUTA, 
        SP_USUARIO.ANO, 
        SP_USUARIO.PERIODO,
        SP_USUARIO.PRIMERAPELLIDO|| ' '|| SP_USUARIO.SEGUNDOAPELLIDO|| ' ' || SP_USUARIO.NOMBRES NOMBREUSUARIO, 
        SP_USUARIO.CODIGOINTERNO,
        SP_USUARIO.DIRTECNICA, 
        SP_USUARIO.DIRGUIA, 
        SP_USUARIO.CODIGOCATASTRAL, 
        SP_USUARIO.USO, 
        SP_USUARIO.ESTRATO,
        SP_MEDIDOR.CODIGO MEDIDOR,
        SP_USUARIO.LECTURA, 
        SP_USUARIO.PERIODOSATRASO,
        DECODE(SP_USUARIO.CHAPETAS,-1, 'X' , 0 , ' ' )CHAPETAS, 
        SP_USUARIO.TOTFACTURAPERACTUAL,
        SUM(SP_FACTURADO.VALOR_FACTURADO+SP_FACTURADO.DEUDA+SP_FACTURADO.VALORFINACT+SP_FACTURADO.VALORFINANT-SP_FACTURADO.VALORABONOACT-SP_FACTURADO.VALORABONOANT-SP_FACTURADO.CREDITOABONADO) AS VLR_FACT,
        DECODE(SP_USUARIO.ESTADO,'A','ACTIVO','R','RETIRADO','C','CORTADO','S','SUSPENDIDO') ESTADO,
        SP_USUARIO.TOTALACUEDUCTO, 
        SP_USUARIO.TOTALALCANTARILLADO, 
        SP_USUARIO.TOTALASEO, 
        SP_USUARIO.NIT 
FROM SP_USUARIO 
  INNER JOIN SP_FACTURADO 
    ON SP_USUARIO.COMPANIA    = SP_FACTURADO.COMPANIA 
    AND SP_USUARIO.CICLO      = SP_FACTURADO.CICLO 
    AND SP_USUARIO.CODIGORUTA = SP_FACTURADO.CODIGORUTA 
    AND SP_USUARIO.ANO        = SP_FACTURADO.ANO 
    AND SP_USUARIO.PERIODO    = SP_FACTURADO.PERIODO
LEFT JOIN SP_MEDIDOR
  ON SP_USUARIO.COMPANIA     = SP_MEDIDOR.COMPANIA 
  AND SP_USUARIO.MEDIDOR     = SP_MEDIDOR.CONSECUTIVO
WHERE SP_USUARIO.COMPANIA     = s$compania$s
  AND SP_USUARIO.CICLO BETWEEN s$cicloInicial$s AND s$cicloFinal$s
  AND SP_USUARIO.BANCOPERPROCESO IS NULL 
  AND SP_USUARIO.USO BETWEEN s$usoInicial$s AND s$usoFinal$s
  AND SP_USUARIO.ESTRATO BETWEEN s$estratoInicial$s AND s$estratoFinal$s 
  s$condicionBarrio$s
  s$condicionIncluir$s
  s$condicionChapetas$s
  s$condicion$s
  AND (SP_FACTURADO.CONCEPTO BETWEEN 1 AND 48 
  AND SP_FACTURADO.CONCEPTO NOT IN(12,17) OR SP_FACTURADO.CONCEPTO IN(201,202,203,204,205,206,207,246,247,248,249)) 
GROUP BY    SP_USUARIO.COMPANIA, 
        SP_USUARIO.CICLO, 
        SP_USUARIO.CODIGORUTA, 
        SP_USUARIO.ANO, 
        SP_USUARIO.PERIODO,
        SP_USUARIO.PRIMERAPELLIDO, 
        SP_USUARIO.SEGUNDOAPELLIDO, 
        SP_USUARIO.NOMBRES, 
        SP_USUARIO.CODIGOINTERNO,
        SP_USUARIO.DIRTECNICA, 
        SP_USUARIO.DIRGUIA, 
        SP_USUARIO.CODIGOCATASTRAL, 
        SP_USUARIO.USO, 
        SP_USUARIO.ESTRATO,
        SP_MEDIDOR.CODIGO, 
        SP_USUARIO.LECTURA, 
        SP_USUARIO.PERIODOSATRASO,
        SP_USUARIO.CHAPETAS, 
        SP_USUARIO.TOTFACTURAPERACTUAL,
        SP_USUARIO.ESTADO,
        SP_USUARIO.TOTALACUEDUCTO, 
        SP_USUARIO.TOTALALCANTARILLADO, 
        SP_USUARIO.TOTALASEO, 
        SP_USUARIO.NIT
s$orderBy$s
