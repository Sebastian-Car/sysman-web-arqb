SELECT SP_USUARIO.COMPANIA,
       SP_USUARIO.CICLO,
       SP_USUARIO.CODIGORUTA,
       SP_USUARIO.CODIGOINTERNO,
       (SP_USUARIO.NOMBRES|| ' ' ||SP_USUARIO.PRIMERAPELLIDO||' '||SP_USUARIO.SEGUNDOAPELLIDO) NOMBRECOMPLETO, 
       SP_USUARIO.DIRTECNICA, 
       SP_USUARIO.NIT, 
       SP_USUARIO.ANO,
       SP_USUARIO.PERIODO,
       SP_USUARIO.USO, 
       SP_USUARIO.ESTRATO,
       SP_USUARIO.ESTRATOASEO,
       VASEOUNICO,
       VASEODOMICILIARIO,
       VASEOBARRIDO,
       VASEOCONSUMO,
       SUBASEO,
       SOBREASEO,
       PESOASEO,
       INDDESHABITADO,
       0 TRAMOEXCEDENTE, 
       0 VALDESHABITADO,
       SP_USUARIO.VASEOUNICO+SP_USUARIO.VASEODOMICILIARIO+SP_USUARIO.VASEOBARRIDO+SP_USUARIO.VASEOCONSUMO TOTAL, 
       (SP_USUARIO.VASEOUNICO+SP_USUARIO.VASEODOMICILIARIO+SP_USUARIO.VASEOBARRIDO+SP_USUARIO.VASEOCONSUMO)+SUBASEO-SOBREASEO PLENA 
  FROM SP_USUARIO
 WHERE SP_USUARIO.COMPANIA = s$compania$s
   AND SP_USUARIO.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
   AND SP_USUARIO.CICLO BETWEEN s$cicloInicial$s AND s$cicloFinal$s
   AND SP_USUARIO.ESTADO <> 'R' 
   AND SP_USUARIO.ASEO NOT IN (0)
