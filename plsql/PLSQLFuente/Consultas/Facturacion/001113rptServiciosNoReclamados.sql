SELECT  CODIGORUTA,
        CODIGOINTERNO,
        TO_CHAR(FECHASOLICITUD, 'DD/MM/YYYY') AS FECHASOLICITUD,
        NUM_PQR,
        FACTURA,
        PERIODO,
        ANO,
        CASE WHEN ACUEDUCTO = 0
              THEN ''
              ELSE 'Acueducto '
        END || CASE WHEN ALCANTARILLADO = 0 
                    THEN  '' 
                    ELSE 'Alcantarillado '
                END || CASE WHEN ASEO = 0 
                            THEN ''
                            ELSE 'Aseo '
                        END AS SERVICIOSFACTURADOS,
        CLASEPROBLEMA FROM(
SELECT  SP_USUARIO.CODIGORUTA,
        SP_USUARIO.CODIGOINTERNO,
        SP_ORDENTRABAJO.FECHASOLICITUD,
        SP_D_ABONOS.NUM_PQR,
        SP_USUARIO.FACTURA,
        SP_D_ABONOS.PERIODO,
        SP_D_ABONOS.ANO,
        SP_CONCEPTOS.CODIGO,
        CASE (SP_D_ORDENTRABAJO.CLASEPROBLEMA)
          WHEN 'ACU' THEN 'Acueducto'
          WHEN 'ALC' THEN 'Alcantarillado'
          WHEN 'ASE' THEN 'Aseo'
        END AS CLASEPROBLEMA
FROM  (SP_USUARIO
        INNER JOIN (SP_CONCEPTOS
          INNER JOIN (SP_ORDENTRABAJO
            INNER JOIN SP_D_ABONOS
              ON SP_ORDENTRABAJO.NUMORDEN    = SP_D_ABONOS.NUM_PQR
              AND SP_ORDENTRABAJO.CODIGORUTA = SP_D_ABONOS.CODIGORUTA
              AND SP_ORDENTRABAJO.COMPANIA   = SP_D_ABONOS.COMPANIA)
              ON SP_CONCEPTOS.COMPANIA       = SP_D_ABONOS.COMPANIA
              AND SP_CONCEPTOS.CODIGO        = SP_D_ABONOS.CONCEPTO)
              ON SP_USUARIO.CODIGORUTA       = SP_D_ABONOS.CODIGORUTA
              AND SP_USUARIO.CICLO           = SP_D_ABONOS.CICLO
              AND SP_USUARIO.COMPANIA        = SP_D_ABONOS.COMPANIA)
                INNER JOIN SP_D_ORDENTRABAJO
                  ON SP_ORDENTRABAJO.NUMORDEN  = SP_D_ORDENTRABAJO.ORDENTRABAJO
                  AND SP_ORDENTRABAJO.COMPANIA = SP_D_ORDENTRABAJO.COMPANIA
                  AND SP_ORDENTRABAJO.CLASEDOC = SP_D_ORDENTRABAJO.CLASEDOC
WHERE SP_D_ABONOS.COMPANIA  = s$compania$s
AND   SP_D_ABONOS.CICLO     = 's$ciclo$s')
PIVOT (
  COUNT(CODIGO)
  FOR CODIGO IN ('01' AS "ACUEDUCTO",'02' AS "ALCANTARILLADO",'03' AS "ASEO")
)
