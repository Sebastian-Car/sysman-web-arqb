SELECT SP_FACTURADO.COMPANIA,
  SP_FACTURADO.CICLO,
  SP_FACTURADO.ANO,
  SP_FACTURADO.PERIODO,
  (DECODE(SP_FACTURADO.PERIODO,'01','Enero','02','Febrero','03','Marzo','04','Abril','05','Mayo','06','Junio','07','Julio','08','Agosto','09','Septiembre','10','Octubre','11','Noviembre','12','Diciembre','') ||'/'|| SP_FACTURADO.ANO) AS PERIODOCOMPLETO,
  SP_FACTURADO.CONCEPTO,
  SUM(SP_FACTURADO.DEUDA)AS DEUDA_FAC,
  SUM(SP_FACTURADO.VALOR_FACTURADO)AS VALOR_FAC,
  SUM(SP_FACTURADO.DEUDA +VALORFINANT-VALORABONOANT) AS DEUDADEFINITIVA,
  SUM(VALOR_FACTURADO + VALORFINACT - VALORABONOACT - SP_FACTURADO. CREDITOABONADO) AS FACTURADODEFINITIVO,
  SUM(SP_FACTURADO.VALOR_FACTURADO + SP_FACTURADO.DEUDA+VALORFINANT+VALORFINACT-VALORABONOANT-VALORABONOACT-SP_FACTURADO.CREDITOABONADO) AS TOTALAPAGAR,
  SP_CONCEPTOS.NOMBRE  AS NOMBRECONCEPTO,
  SP_USUARIO.PERIODOSATRASO
FROM SP_USUARIO
INNER JOIN (SP_FACTURADO
  LEFT JOIN SP_CONCEPTOS
    ON  SP_FACTURADO.COMPANIA   = SP_CONCEPTOS.COMPANIA
    AND SP_FACTURADO.CONCEPTO  = SP_CONCEPTOS.CODIGO)
  ON  SP_USUARIO.COMPANIA     = SP_FACTURADO.COMPANIA
  AND SP_USUARIO.CICLO       = SP_FACTURADO.CICLO
  AND SP_USUARIO.CODIGORUTA  = SP_FACTURADO.CODIGORUTA
  AND SP_USUARIO.ANO         = SP_FACTURADO.ANO
  AND SP_USUARIO.PERIODO     = SP_FACTURADO.PERIODO
WHERE SP_FACTURADO.COMPANIA = 's$compania$s'
  AND SP_FACTURADO.CICLO 
  BETWEEN 's$cicloInicial$s' AND 's$cicloFinal$s'
  AND (SP_FACTURADO.CONCEPTO 
  BETWEEN 1 AND 48 
  OR SP_FACTURADO.CONCEPTO  IN (201,202,203,204,205,206,207,246,247,248,249)
  AND SP_FACTURADO.CONCEPTO NOT IN(12,17))
  s$condicion$s
  AND BANCOPERPROCESO    IS NULL
  s$incluirSuspendidosRetirados$s
GROUP BY SP_FACTURADO.COMPANIA,
  SP_FACTURADO.CICLO,
  SP_FACTURADO.ANO,
  SP_FACTURADO.PERIODO,
  SP_FACTURADO.CONCEPTO,
  SP_CONCEPTOS.NOMBRE,
  SP_USUARIO.PERIODOSATRASO
HAVING  SUM(SP_FACTURADO.DEUDA +VALORFINANT-VALORABONOANT)  +
  SUM(VALOR_FACTURADO + VALORFINACT - VALORABONOACT - SP_FACTURADO. CREDITOABONADO) +
 SUM(SP_FACTURADO.VALOR_FACTURADO + SP_FACTURADO.DEUDA+VALORFINANT+VALORFINACT-VALORABONOANT-VALORABONOACT-SP_FACTURADO.CREDITOABONADO) <>0
