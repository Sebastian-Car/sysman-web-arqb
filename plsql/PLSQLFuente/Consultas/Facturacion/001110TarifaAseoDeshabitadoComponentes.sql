SELECT  SP_TARIFAS.DES_ASEOUNICO, 
        SP_TARIFAS.DES_ASEDOMICILIARIO, 
        SP_TARIFAS.DES_ASEBARRIDO, 
        SP_TARIFAS.DES_ASEOCONSUMO, 
        SP_TARIFAS.DES_TRAMOEXCEDENTE, 
        SP_USOS.NOMBRE AS NOMBRE_USO, 
        SP_ESTRATOS.NOMBRE AS NOMBRE_ESTRATO, 
        COMPANIA.RUTA_VIGILADO_POR AS VIGILADOPOR 
FROM  SP_TARIFAS 
  LEFT JOIN SP_USOS 
    ON SP_TARIFAS.COMPANIA = SP_USOS.COMPANIA 
    AND SP_TARIFAS.USO     = SP_USOS.CODIGO 
      LEFT JOIN SP_ESTRATOS 
        ON SP_TARIFAS.COMPANIA      = SP_ESTRATOS.COMPANIA 
        AND SP_TARIFAS.USO          = SP_ESTRATOS.USO 
        AND SP_TARIFAS.ESTRATO      = SP_ESTRATOS.CODIGO 
          LEFT JOIN COMPANIA  
            ON SP_TARIFAS.COMPANIA  = COMPANIA.CODIGO  
WHERE SP_TARIFAS.COMPANIA   = s$compania$s 
AND   SP_TARIFAS.ANO        = s$ano$s 
AND   SP_TARIFAS.PERIODO    = 's$mes$s' 
ORDER BY  SP_TARIFAS.COMPANIA, 
          SP_TARIFAS.ANO, 
          SP_TARIFAS.PERIODO
