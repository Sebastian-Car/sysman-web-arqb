SELECT SP_USUARIO.COMPANIA,
       SP_USUARIO.CICLO,
       SP_USUARIO.CODIGORUTA,
       SP_USUARIO.ANO,
       SP_USUARIO.PERIODO,
       (SP_USUARIO.PRIMERAPELLIDO||' '||SP_USUARIO.SEGUNDOAPELLIDO||' '||SP_USUARIO.NOMBRES) NOMBRE,
       SP_USUARIO.CODIGOINTERNO, 
       SP_USUARIO.DIRTECNICA,
       SP_USUARIO.DIRGUIA,
       SP_USUARIO.USO,
       SP_USUARIO.LECTURA1,
       SP_USUARIO.LECTURA,
       SP_USUARIO.LECTURAAFORO,
       SP_USUARIO.CONSUMO,
       SP_USUARIO.CONSUMOPROM,
       SUM(CASE WHEN (LECTURA1=LECTURA AND CASE WHEN (SP_USUARIO.INDDESHABITADO<>0 AND SP_USUARIO.CHAPETAS<>0) OR (SP_USUARIO.INDDESHABITADO=0 AND SP_USUARIO.CHAPETAS<>0) OR (SP_USUARIO.INDDESHABITADO<>0 AND SP_USUARIO.CHAPETAS=0) THEN -1 ELSE 0 END =-1 AND LECTURA<>0) OR (LECTURA1>LECTURA AND CASE WHEN SP_MEDIDOR.CODIGO <> ' ' AND SP_MEDIDOR.ELEMCAMBIO='Medidor' THEN -1 ELSE 0 END=-1) OR (LECTURA>LECTURA1) THEN 1 ELSE 0 END) MED_BUENOS,
       SUM(CASE WHEN (LECTURA1=LECTURA AND CASE WHEN (SP_USUARIO.INDDESHABITADO<>0 AND SP_USUARIO.CHAPETAS<>0) OR (SP_USUARIO.INDDESHABITADO=0 AND SP_USUARIO.CHAPETAS<>0) OR (SP_USUARIO.INDDESHABITADO<>0 AND SP_USUARIO.CHAPETAS=0) THEN -1 ELSE 0 END = 0 AND LECTURA<>0) OR (LECTURA1>LECTURA AND CASE WHEN SP_MEDIDOR.CODIGO <> ' ' AND SP_MEDIDOR.ELEMCAMBIO='Medidor' THEN -1 ELSE 0 END=0) THEN 1 ELSE 0 END) MED_MALOS,
       CASE WHEN LECTURAAFORO=0 AND LECTURA=0 THEN 'Sin lectura' ELSE CASE WHEN LECTURA-LECTURA1<0 THEN 'Medidor invertido' ELSE CASE WHEN LECTURA-LECTURA1=0 THEN 'Medidor parado' ELSE CASE WHEN LECTURA-LECTURA1>10000 THEN 'Consumo elevado' ELSE 'Normal' END END END END OBSERVACIONES,          
       SP_USUARIO.PROBLEMALECTURA INDPROBLEMA,
       LECTURA-LECTURA1 TOTALCONSUMO,
       SP_USUARIO.MEDIDOR,
       SP_USUARIO.ESTRATO 
  FROM SP_USUARIO
    LEFT JOIN SP_MEDIDOR 
       ON SP_USUARIO.COMPANIA = SP_MEDIDOR.COMPANIA
      AND SP_USUARIO.ANO = SP_MEDIDOR.ANO
      AND SP_USUARIO.PERIODO = SP_MEDIDOR.PERIODO
      AND SP_USUARIO.CICLO = SP_MEDIDOR.CICLO
      AND SP_USUARIO.CODIGORUTA = SP_MEDIDOR.CODIGORUTA
      AND SP_USUARIO.MEDIDOR = SP_MEDIDOR.CONSECUTIVO
 WHERE SP_USUARIO.COMPANIA = s$compania$s
  s$filtroCiclo$s
  s$filtroIndProblema$s
  s$condicion$s
  AND SP_USUARIO.ACUEDUCTO NOT IN (0)
  AND SP_USUARIO.ESTADO NOT IN ('R')
GROUP BY 
       SP_USUARIO.COMPANIA,
       SP_USUARIO.CICLO,
       SP_USUARIO.CODIGORUTA,
       SP_USUARIO.ANO,
       SP_USUARIO.PERIODO,
       (SP_USUARIO.PRIMERAPELLIDO||' '||SP_USUARIO.SEGUNDOAPELLIDO||' '||SP_USUARIO.NOMBRES),
       SP_USUARIO.CODIGOINTERNO, 
       SP_USUARIO.DIRTECNICA,
       SP_USUARIO.DIRGUIA,
       SP_USUARIO.USO,
       SP_USUARIO.LECTURA1,
       SP_USUARIO.LECTURA,
       SP_USUARIO.LECTURAAFORO,
       SP_USUARIO.CONSUMO,
       SP_USUARIO.CONSUMOPROM,
       SP_MEDIDOR.ELEMCAMBIO,
       SP_MEDIDOR.CODIGO,
       SP_USUARIO.INDDESHABITADO,
       SP_USUARIO.CHAPETAS,
       CASE WHEN LECTURAAFORO=0 AND LECTURA=0 THEN 'Sin lectura' ELSE CASE WHEN LECTURA-LECTURA1<0 THEN 'Medidor invertido' ELSE CASE WHEN LECTURA-LECTURA1=0 THEN 'Medidor parado' ELSE CASE WHEN LECTURA-LECTURA1>10000 THEN 'Consumo elevado' ELSE 'Normal' END END END END,          
       SP_USUARIO.PROBLEMALECTURA,
       LECTURA-LECTURA1,
       SP_USUARIO.MEDIDOR,
       SP_USUARIO.ESTRATO
