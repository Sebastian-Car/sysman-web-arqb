SELECT V_SP_RECAUDOS.BANCO,
  V_SP_RECAUDOS.BANCO_NOM,
  SUM(V_SP_RECAUDOS.VALORREGISTRADO) AS valorregistradoF,
	SUM(V_SP_RECAUDOS.CONVENIOS) AS TotalRaycoF,
	SUM(V_SP_RECAUDOS.TERCERIZADO) AS TotalTercerizadoF,
	SUM(V_SP_RECAUDOS.TOTAL) AS totalH,
	COUNT(V_SP_RECAUDOS.NUMEROPAQUETE) AS CuentaDeNUMEROPAQUETE 
FROM V_SP_RECAUDOS
WHERE 
  V_SP_RECAUDOS.COMPANIA = 's$compania$s' 
  AND V_SP_RECAUDOS.FECHA 
    BETWEEN s$fechaInicial$s
    AND s$fechaFinal$s
GROUP BY V_SP_RECAUDOS.BANCO, V_SP_RECAUDOS.BANCO_NOM, V_SP_RECAUDOS.COMPANIA
ORDER BY V_SP_RECAUDOS.BANCO
