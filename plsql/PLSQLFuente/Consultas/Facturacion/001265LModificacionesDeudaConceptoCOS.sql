SELECT SP_USUARIO.COMPANIA, 
       SP_USUARIO.CICLO, 
       SP_USUARIO.CODIGORUTA, 
       SUM(CASE WHEN SP_USUARIO.INDDEUDAFINANCIADA IN (0) THEN (SP_FACTURADO.DEUDA-(SP_FACTURADO.VALOR_FACTURADOANT+SP_FACTURADO.DEUDAANT)) ELSE 0 END) AS MODIFICACION, 
       SUM(CASE WHEN SP_USUARIO.INDDEUDAFINANCIADA NOT IN (0) THEN (SP_FACTURADO.DEUDA - (SP_FACTURADO.VALOR_FACTURADOANT+SP_FACTURADO.DEUDAANT)) ELSE 0 END) AS FINANCIACION, 
       SP_CONCEPTOS.CODIGO, 
       SP_CONCEPTOS.NOMBRE AS NOMBRECONCEPTO,        
       SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO || ' ' ||  SP_USUARIO.NOMBRES AS NOMBRECOMPLETO, 
       SP_FACTURADO.DEUDA, 
       (SP_FACTURADO.VALOR_FACTURADOANT+SP_FACTURADO.DEUDAANT)-SP_FACTURADO.DEUDA AS DIFERENCIA, 
       (SP_FACTURADO.VALOR_FACTURADOANT+SP_FACTURADO.DEUDAANT) AS TOTALANTERIOR
  FROM SP_USUARIO 
       INNER JOIN SP_CICLO 
               ON SP_USUARIO.COMPANIA = SP_CICLO.COMPANIA
              AND SP_USUARIO.CICLO    = SP_CICLO.NUMERO 
              AND SP_USUARIO.ANO      = SP_CICLO.ANO
              AND SP_USUARIO.PERIODO  = SP_CICLO.PERIODO
       INNER JOIN (SP_FACTURADO  
                    INNER JOIN SP_CONCEPTOS 
                           ON SP_FACTURADO.COMPANIA = SP_CONCEPTOS.COMPANIA  
                          AND SP_FACTURADO.CONCEPTO = SP_CONCEPTOS.CODIGO) 
               ON SP_USUARIO.COMPANIA   = SP_FACTURADO.COMPANIA 
              AND SP_USUARIO.CICLO      = SP_FACTURADO.CICLO 
              AND SP_USUARIO.CODIGORUTA = SP_FACTURADO.CODIGORUTA 
              AND SP_USUARIO.ANO        = SP_FACTURADO.ANO 
              AND SP_USUARIO.PERIODO    = SP_FACTURADO.PERIODO  
 WHERE SP_USUARIO.COMPANIA = s$compania$s 
   AND SP_USUARIO.CICLO = s$ciclo$s 
   AND SP_USUARIO.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'    
   AND SP_USUARIO.BANCOPERANTERIOR IS NULL 
   AND (SP_FACTURADO.CONCEPTO BETWEEN 1 AND 49 
    OR SP_FACTURADO.CONCEPTO IN (201,202,203,204,205,206,207,246,247,248,249)) 
 GROUP BY SP_USUARIO.COMPANIA, 
          SP_USUARIO.CICLO, 
          SP_USUARIO.CODIGORUTA, 
          SP_CONCEPTOS.CODIGO, 
          SP_CONCEPTOS.NOMBRE,    
          SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO || ' ' ||  SP_USUARIO.NOMBRES, 
          SP_FACTURADO.DEUDA, 
          (SP_FACTURADO.VALOR_FACTURADOANT+SP_FACTURADO.DEUDAANT)-SP_FACTURADO.DEUDA, 
          (SP_FACTURADO.VALOR_FACTURADOANT+SP_FACTURADO.DEUDAANT) 
HAVING SUM(SP_FACTURADO.DEUDA-(SP_FACTURADO.VALOR_FACTURADOANT+SP_FACTURADO.DEUDAANT))<> 0 
