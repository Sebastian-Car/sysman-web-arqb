SELECT DECODE(FECHA,'1','Enero','2','Febrero','3','Marzo','4','Abril','5','Mayo','6','Junio','7','Julio','8','Agosto','9','Septiembre','10','Octubre','11','Noviembre','12','Diciembre','')AS FECHA, FECHA FECHAORDEN,'s$compania$s' COMPANIA, 
       C1_P C1P,C2_P C2P,C3_P C3P,C4_P C4P,C5_P C5P,C6_P C6P, 
       C1_C C1C,C2_C C2C,C3_C C3C,C4_C C4C,C5_C C5C,C6_C C6C, 
       C1_S C1S,C2_S C2S,C3_S C3S,C4_S C4S,C5_S C5S,C6_S C6S, 
       CIA, NIT, SIGLACOMPANIA, '1' MES, '1' ANO 
FROM (SELECT EXTRACT(MONTH FROM FECHACREADA) FECHA, 
             SP_DESVIACIONES.CICLO, 
             CASE WHEN SP_DESVIACIONES.FECHACREADA <= TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND (NVL(SP_DESVIACIONES.FECHACIERRE, '') = '' OR SP_DESVIACIONES.FECHACIERRE  > TO_DATE('s$fechaFinal$s','DD/MM/YYYY')) 
             THEN SP_DESVIACIONES.CONSECUTIVO ELSE 0 END TOTAL, 
             CASE WHEN ESTADO='C' THEN SP_DESVIACIONES.CONSECUTIVO ELSE 0 END TOTALS, 
             SP_DESVIACIONES.CONSECUTIVO TOTALC, 
             COMPANIA.SIGLACOMPANIA, 
             COMPANIA.NOMBRE AS CIA, 
             COMPANIA.NITCOMPANIA AS NIT  
       FROM SP_DESVIACIONES 
        INNER JOIN COMPANIA 
          ON SP_DESVIACIONES.Compania = COMPANIA.CODIGO 
       WHERE SP_DESVIACIONES.COMPANIA='s$compania$s') 
PIVOT (COUNT(TOTAL) P, COUNT (TOTALS) S, COUNT (TOTALC) C FOR CICLO IN (1 AS C1, 2 AS C2,3 AS C3,4 C4,5 C5,6 C6)) ORDER BY FECHAORDEN ASC
