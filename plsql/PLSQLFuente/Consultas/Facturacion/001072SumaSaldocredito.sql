SELECT 
   SALDOCREDITOPERIODO.COMPANIA, 
   SALDOCREDITOPERIODO.CICLO, 
   SALDOCREDITOPERIODO.CODIGORUTA, 
   SALDOCREDITOPERIODO.ANO, 
   SALDOCREDITOPERIODO.PERIODO, 
   SALDOCREDITOPERIODO.VALOR_FACTURADO, 
   SUM (SP_FACTURADO.CREDITOABONADO) SUMAAB
FROM (SELECT 
        SP_FACTURADO.COMPANIA, 
        SP_FACTURADO.CICLO, 
        SP_FACTURADO.CODIGORUTA, 
        SP_FACTURADO.ANO, 
        SP_FACTURADO.PERIODO, 
        SP_FACTURADO.CONCEPTO, 
        SP_FACTURADO.VALOR_FACTURADO
      FROM SP_CICLO  
      INNER JOIN SP_FACTURADO  
         ON SP_CICLO .PERIODO = SP_FACTURADO.PERIODO  
        AND SP_CICLO .ANO = SP_FACTURADO.ANO   
        AND SP_CICLO .NUMERO = SP_FACTURADO.CICLO  
        AND SP_CICLO .COMPANIA = SP_FACTURADO.COMPANIA
      WHERE SP_CICLO.COMPANIA= s$compania$s 
        AND SP_FACTURADO.CONCEPTO=17  
        AND SP_FACTURADO.VALOR_FACTURADO <> 0)SALDOCREDITOPERIODO  
INNER JOIN SP_FACTURADO  
   ON SALDOCREDITOPERIODO.PERIODO = SP_FACTURADO.PERIODO  
  AND SALDOCREDITOPERIODO.ANO = SP_FACTURADO.ANO  
  AND SALDOCREDITOPERIODO.CODIGORUTA = SP_FACTURADO.CODIGORUTA  
  AND SALDOCREDITOPERIODO.CICLO = SP_FACTURADO.CICLO
  AND SALDOCREDITOPERIODO.COMPANIA = SP_FACTURADO.COMPANIA 
WHERE SALDOCREDITOPERIODO.COMPANIA=s$compania$s  
  AND ((SP_FACTURADO.CONCEPTO BETWEEN 1 AND 48 OR SP_FACTURADO.CONCEPTO IN (246,247,248,249)) AND SP_FACTURADO.CONCEPTO NOT IN (12,17)) 
GROUP BY SALDOCREDITOPERIODO.COMPANIA, SALDOCREDITOPERIODO.CICLO, SALDOCREDITOPERIODO.CODIGORUTA, SALDOCREDITOPERIODO.ANO, SALDOCREDITOPERIODO.PERIODO, SALDOCREDITOPERIODO.VALOR_FACTURADO
HAVING ( SUM (SP_FACTURADO.CREDITOABONADO) <> -(SALDOCREDITOPERIODO.VALOR_FACTURADO))
