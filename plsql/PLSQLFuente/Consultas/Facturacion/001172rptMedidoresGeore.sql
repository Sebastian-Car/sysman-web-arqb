SELECT SP_USUARIO.CICLO,
  SP_USUARIO.CODIGORUTA,
  SP_USUARIO.PRIMERAPELLIDO||' '||  SP_USUARIO.SEGUNDOAPELLIDO||' '||  SP_USUARIO.NOMBRES AS NOMBRE,
  SP_USUARIO.DIRGUIA,
  SP_USUARIO.CODIGOINTERNO,
  SP_MEDIDOR.CODIGO,
 SP_MEDIDOR.GEO_VERTICE,
  SP_MEDIDOR.GEO_LONGITUD,
  SP_MEDIDOR.GEO_LATITUD,
  SP_MEDIDOR.GEO_ALTITUD,
  SP_USUARIO.DIRTECNICA
FROM SP_USUARIO
INNER JOIN SP_MEDIDOR
ON SP_USUARIO.COMPANIA     = SP_MEDIDOR.COMPANIA 
AND SP_USUARIO.CICLO       = SP_MEDIDOR.CICLO
AND SP_USUARIO.CODIGORUTA  = SP_MEDIDOR.CODIGORUTA
WHERE SP_MEDIDOR.COMPANIA=s$compania$s
s$ciclo$s
s$codigoRuta$s
s$vertice$s
ORDER BY CICLO,CODIGORUTA,CODIGOINTERNO
