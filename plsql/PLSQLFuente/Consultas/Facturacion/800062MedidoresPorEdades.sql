SELECT  SP_USUARIO.CICLO, 
        SP_USUARIO.CODIGORUTA, 
        SP_USUARIO.CODIGOINTERNO, 
        CUENTAMEDIDOR.MEDIDOR, 
        TRIM(SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO || ' ' || SP_USUARIO.NOMBRES) AS NOMBRES, 
        TO_CHAR(SP_USUARIO.FECHAINSTALACION,'DD/MM/YYYY') AS FECHAINSTALACION 
FROM (SELECT SP_USUARIO.COMPANIA,
                        SP_USUARIO.CICLO,
                        SP_USUARIO.MEDIDOR,
                        COUNT(SP_USUARIO.MEDIDOR) AS REPETIDOS
           FROM SP_USUARIO
          WHERE SP_USUARIO.COMPANIA =s$compania$s
          GROUP BY SP_USUARIO.COMPANIA,
                           SP_USUARIO.CICLO,
                            SP_USUARIO.MEDIDOR
             HAVING COUNT(SP_USUARIO.MEDIDOR) > 1
            ORDER BY COUNT(SP_USUARIO.MEDIDOR)) CUENTAMEDIDOR 
  LEFT JOIN SP_USUARIO 
    ON CUENTAMEDIDOR.COMPANIA = SP_USUARIO.COMPANIA 
    AND CUENTAMEDIDOR.CICLO = SP_USUARIO.CICLO 
    AND CUENTAMEDIDOR.MEDIDOR = SP_USUARIO.MEDIDOR
ORDER BY CUENTAMEDIDOR.MEDIDOR , 
         TRIM(SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO || ' ' || SP_USUARIO.NOMBRES)
