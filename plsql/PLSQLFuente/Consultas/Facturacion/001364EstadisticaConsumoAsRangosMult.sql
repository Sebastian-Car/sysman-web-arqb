SELECT QRY_PESOASEOESTTOT.COMPANIA,
  QRY_PESOASEOESTTOT.NUMUSUARIOS,
  QRY_PESOASEOESTTOT.PERIODOUNICO,
  QRY_PESOASEOESTTOT.TIPOPRODUCTOR,
 CASE WHEN  NVL(PCK_SYSMAN_UTL.FC_PAR(s$compania$s,'PESO ASEO EN TONELADAS','74',SYSDATE),'NO') = 'NO' THEN SUMADEPESOASEO ELSE SUMADEPESOASEO*0.25 END  PESOASEO,
  QRY_DESHABITADO_ASEO.SUSCASEODESH,
  QRY_PESOASEOESTTOT.ANO,
  QRY_PESOASEOESTTOT.PERIODO,
  QRY_PESOASEOESTTOT.USO,
  QRY_PESOASEOESTTOT.ESTRATO,
  QRY_PESOASEOESTTOT.SUMADECONCEPTO21,
  QRY_PESOASEOESTTOT.SUMADECONCEPTO20,
  QRY_PESOASEOESTTOT.SUMADECONCEPTO22,
  QRY_PESOASEOESTTOT.SUMADECONCEPTO3,
  QRY_PESOASEOESTTOT.SUMADECONCEPTO48,
  CASE WHEN NVL(SUMADECONCEPTO46,'')<>'' THEN SUMADECONCEPTO46 ELSE 0  END AS SUMACONCEPTO46,
  QRY_PESOASEOESTTOT.SUMADESUMADETOTFACTURAASEO,
  QRY_PESOASEOESTTOT.USO||' - '|| SP_USOS.NOMBRE NOMBREUSO,

  (QRY_PESOASEOESTTOT.SUMADECONCEPTO21+QRY_PESOASEOESTTOT.SUMADECONCEPTO20+QRY_PESOASEOESTTOT.SUMADECONCEPTO22+QRY_PESOASEOESTTOT.SUMADECONCEPTO3+QRY_PESOASEOESTTOT.SUMADECONCEPTO48) AS TOTASEO,
  SUM(CASE WHEN CONCEPTO IN (3,20,21,22,46,48) THEN  VALOR_FACTURADO ELSE 0 END )                                                                                                                       AS VLRFACTURADOASEO,
  SUM(CASE WHEN CONCEPTO IN (3,20,21,22,46,48)  THEN RECAUDADOPERIODO ELSE 0 END )                                                                                                                      AS VLRRECAUDADOASEO,
  NVL(CANTIDADM,0)                                                                                                                                                                                        AS CANTIDADMULT,
   CASE WHEN  NVL(PCK_SYSMAN_UTL.FC_PAR('001','PESO ASEO EN TONELADAS','74',SYSDATE),'NO') = 'NO' THEN NVL(MULTIUSUARIOAGRUPADO.PESOASEOM,0) ELSE NVL(MULTIUSUARIOAGRUPADO.PESOASEOM,0)*0.25 END  AS PESOASEOMULT
                                                                  
FROM (SP_ESTADISTICAS_FACTURACION ESTADISTICAS_FACTURACION
INNER JOIN ((SELECT MULTIUSUARIOS.COMPANIA,
                    MULTIUSUARIOS.CICLO,
                    MULTIUSUARIOS.USO,
                    MULTIUSUARIOS.ESTRATO,
                    COUNT(MULTIUSUARIOS.CANTIDAD) AS CANTIDADM,
                    SUM(MULTIUSUARIOS.PESOASEO)   AS PESOASEOM
                  FROM SP_MULTIUSUARIOS MULTIUSUARIOS
                  GROUP BY MULTIUSUARIOS.COMPANIA,
                    MULTIUSUARIOS.CICLO,
                    MULTIUSUARIOS.USO,
                    MULTIUSUARIOS.ESTRATO) MULTIUSUARIOAGRUPADO
                    
RIGHT JOIN SP_QRY_PESOASEOESTTOT QRY_PESOASEOESTTOT
ON (MULTIUSUARIOAGRUPADO.CICLO         = QRY_PESOASEOESTTOT.CICLO)
AND (MULTIUSUARIOAGRUPADO.ESTRATO      = QRY_PESOASEOESTTOT.ESTRATO)
AND (MULTIUSUARIOAGRUPADO.USO          = QRY_PESOASEOESTTOT.USO)
AND (MULTIUSUARIOAGRUPADO.COMPANIA     = QRY_PESOASEOESTTOT.COMPANIA))
ON (ESTADISTICAS_FACTURACION.CICLO     = QRY_PESOASEOESTTOT.CICLO)
AND (ESTADISTICAS_FACTURACION.ESTRATO  = QRY_PESOASEOESTTOT.ESTRATO)
AND (ESTADISTICAS_FACTURACION.USO      = QRY_PESOASEOESTTOT.USO)
AND (ESTADISTICAS_FACTURACION.PERIODO  = QRY_PESOASEOESTTOT.PERIODO)
AND (ESTADISTICAS_FACTURACION.ANO      = QRY_PESOASEOESTTOT.ANO)
AND (ESTADISTICAS_FACTURACION.COMPANIA = QRY_PESOASEOESTTOT.COMPANIA))
INNER JOIN (SELECT ESTADISTICAS_CONSUMO.COMPANIA,
                    ESTADISTICAS_CONSUMO.ANO,
                    ESTADISTICAS_CONSUMO.PERIODO,
                    ESTADISTICAS_CONSUMO.USO,
                    ESTADISTICAS_CONSUMO.ESTRATO,
                    ESTADISTICAS_CONSUMO.CICLO,
                    SUM(ESTADISTICAS_CONSUMO.CANTUSUARIOASEODESH) AS SUSCASEODESH
                  FROM SP_ESTADISTICAS_CONSUMO ESTADISTICAS_CONSUMO
                  WHERE (((ESTADISTICAS_CONSUMO.COMPANIA)=s$compania$s)
                  AND ((ESTADISTICAS_CONSUMO.PERIODO) BETWEEN s$periodoInicial$s AND s$periodoFinal$s)
                  AND ESTADISTICAS_CONSUMO.ANO BETWEEN s$anoInicial$s AND s$anoFinal$s
)
                  GROUP BY ESTADISTICAS_CONSUMO.COMPANIA,
                    ESTADISTICAS_CONSUMO.ANO,
                    ESTADISTICAS_CONSUMO.PERIODO,
                    ESTADISTICAS_CONSUMO.USO,
                    ESTADISTICAS_CONSUMO.ESTRATO,
                    ESTADISTICAS_CONSUMO.CICLO) QRY_DESHABITADO_ASEO
ON (ESTADISTICAS_FACTURACION.ESTRATO   = QRY_DESHABITADO_ASEO.ESTRATO)
AND (ESTADISTICAS_FACTURACION.USO      = QRY_DESHABITADO_ASEO.USO)
AND (ESTADISTICAS_FACTURACION.CICLO    = QRY_DESHABITADO_ASEO.CICLO)
AND (ESTADISTICAS_FACTURACION.PERIODO  = QRY_DESHABITADO_ASEO.PERIODO)
AND (ESTADISTICAS_FACTURACION.ANO      = QRY_DESHABITADO_ASEO.ANO)
AND (ESTADISTICAS_FACTURACION.COMPANIA = QRY_DESHABITADO_ASEO.COMPANIA)
LEFT JOIN  SP_USOS
ON SP_USOS.COMPANIA=Qry_PesoAseoEstTot.COMPANIA
AND SP_USOS.CODIGO=Qry_PesoAseoEstTot.USO

WHERE (((Qry_PesoAseoEstTot.ANO)  BETWEEN s$anoInicial$s AND s$anoFinal$s)
AND ((Qry_PesoAseoEstTot.PERIODO)  BETWEEN s$periodoInicial$s AND s$periodoFinal$s))
s$cicloCondicion$s
GROUP BY 
QRY_PESOASEOESTTOT.COMPANIA,
  QRY_PESOASEOESTTOT.NUMUSUARIOS,
  QRY_PESOASEOESTTOT.PERIODOUNICO,
  QRY_PESOASEOESTTOT.TIPOPRODUCTOR,
 CASE WHEN  NVL(PCK_SYSMAN_UTL.FC_PAR(s$compania$s,'PESO ASEO EN TONELADAS','74',SYSDATE),'NO') = 'NO' THEN SUMADEPESOASEO ELSE SUMADEPESOASEO*0.25 END ,
  QRY_DESHABITADO_ASEO.SUSCASEODESH,
  QRY_PESOASEOESTTOT.ANO,
  QRY_PESOASEOESTTOT.PERIODO,
  QRY_PESOASEOESTTOT.USO,
  QRY_PESOASEOESTTOT.ESTRATO,
  QRY_PESOASEOESTTOT.SUMADECONCEPTO21,
  QRY_PESOASEOESTTOT.SUMADECONCEPTO20,
  QRY_PESOASEOESTTOT.SUMADECONCEPTO22,
  QRY_PESOASEOESTTOT.SUMADECONCEPTO3,
  QRY_PESOASEOESTTOT.SUMADECONCEPTO48,
  CASE WHEN NVL(SUMADECONCEPTO46,'')<>'' THEN SUMADECONCEPTO46 ELSE 0  END,
  QRY_PESOASEOESTTOT.SUMADESUMADETOTFACTURAASEO,
QRY_PESOASEOESTTOT.USO||' - '|| SP_USOS.NOMBRE,
  (QRY_PESOASEOESTTOT.SUMADECONCEPTO21+QRY_PESOASEOESTTOT.SUMADECONCEPTO20+QRY_PESOASEOESTTOT.SUMADECONCEPTO22+QRY_PESOASEOESTTOT.SUMADECONCEPTO3+QRY_PESOASEOESTTOT.SUMADECONCEPTO48) ,
  NVL(CANTIDADM,0),
  CASE WHEN  NVL(PCK_SYSMAN_UTL.FC_PAR(s$compania$s,'PESO ASEO EN TONELADAS','74',SYSDATE),'NO') = 'NO' THEN NVL(MULTIUSUARIOAGRUPADO.PESOASEOM,0) ELSE NVL(MULTIUSUARIOAGRUPADO.PESOASEOM,0)*0.25 END  
