SELECT QRY_COBERTURAMICRO.COMPANIA,
QRY_COBERTURAMICRO.PERIODO ,
  QRY_COBERTURAMICRO.ANO,
PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(s$compania$s,QRY_COBERTURAMICRO.ANO,QRY_COBERTURAMICRO.PERIODO,null) PERIODOCOMPLETO,  
  SP_USOS.NOMBRE USO,
  QRY_COBERTURAMICRO.ESTRATO,
  SUM(QRY_COBERTURAMICRO.ACTIVOS)    AS ACTIVOS,
  SUM(QRY_COBERTURAMICRO.CONMEDIDOR) AS CONMEDIDOR,
  SUM(QRY_COBERTURAMICRO.SINMEDIDOR) AS SINMEDIDOR
FROM (s$subConsulta$s) QRY_COBERTURAMICRO
  INNER JOIN SP_USOS 
  ON SP_USOS.COMPANIA = QRY_COBERTURAMICRO.COMPANIA
  AND SP_USOS.CODIGO= QRY_COBERTURAMICRO.USO 
  group by QRY_COBERTURAMICRO.COMPANIA,
  QRY_COBERTURAMICRO.ANO,
  PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO('001',QRY_COBERTURAMICRO.ANO,QRY_COBERTURAMICRO.PERIODO,null), 
  SP_USOS.NOMBRE, 
  QRY_COBERTURAMICRO.ESTRATO
,
QRY_COBERTURAMICRO.PERIODO 
ORDER BY QRY_COBERTURAMICRO.PERIODO DESC
