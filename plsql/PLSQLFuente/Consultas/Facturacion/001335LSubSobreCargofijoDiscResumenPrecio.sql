WITH FACTURADOCONCEPTO12 AS (
  SELECT SP_FACTURADO.COMPANIA, 
          SP_FACTURADO.CICLO, 
          SP_FACTURADO.CODIGORUTA, 
          SUM(CASE WHEN CONCEPTO IN (1) THEN VALOR_FACTURADOIN ELSE 0 END ) AS ACUEDUCTO, 
          SUM(CASE WHEN CONCEPTO IN (4) THEN VALOR_FACTURADOIN ELSE 0 END ) AS ALCANTARILLADO, 
          CASE  WHEN SUM(SP_FACTURADO.VALOR_FACTURADO) = SUM(CASE WHEN CONCEPTO IN (12) THEN VALOR_FACTURADO ELSE 0 END) 
                 AND SUM(CASE WHEN CONCEPTO IN (12) THEN VALOR_FACTURADO ELSE 0 END) NOT IN (0) 
                THEN -1 
                ELSE 0 
          END AS FINAN 
        FROM SP_FACTURADO 
        GROUP BY SP_FACTURADO.COMPANIA, 
        SP_FACTURADO.CICLO, 
        SP_FACTURADO.CODIGORUTA)
SELECT SP_USUARIO.CICLO,  
        SP_ESTRATOS.NOMBRE         AS ESTRATO,  
        SP_USOS.NOMBRE             AS USO,  
        SUM(SP_USUARIO.SUBFIJO)    AS SUBFIJO,  
        SUM(SP_USUARIO.SOBREFIJO)  AS SOBREFIJO,  
        SUM(CASE WHEN SP_USUARIO.CARGOFIJO IN (0) THEN 0 ELSE SP_USUARIO.CARGOFIJO END)                                                       AS CARGOFIJO,  
        SUM(CASE WHEN SP_USUARIO.CARGOFIJO IN (0) AND FACTURADOCONCEPTO12.FINAN IN(-1) THEN FACTURADOCONCEPTO12.ACUEDUCTO ELSE 0 END)         AS OTROSCARGOFIJO, 
        SUM(CASE WHEN SP_USUARIO.CARGOFIJOAL IN (0) THEN 0 ELSE SP_USUARIO.CARGOFIJOAL END)                                                  AS CARGOFIJOAL, 
        SUM(CASE WHEN SP_USUARIO.CARGOFIJOAL IN (0) AND FACTURADOCONCEPTO12.FINAN IN(-1) THEN FACTURADOCONCEPTO12.ALCANTARILLADO ELSE 0 END ) AS OTROSCARGOFIJOAL, 
        PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(SP_USUARIO.COMPANIA,SP_USUARIO.ANO,SP_USUARIO.PERIODO,NULL)                                   AS NOMPERIODO, 
        SUM(SP_USUARIO.SUBFIJOALC)   AS SUBFIJOALC, 
        SUM(SP_USUARIO.SOBREFIJOALC) AS SOBREFIJOALC, 
        SP_USUARIO.ANO, 
        SP_USUARIO.PERIODO, 
        (SUM(CASE WHEN SP_USUARIO.CARGOFIJO IN (0) 
                  THEN 0 
                  ELSE SP_USUARIO.CARGOFIJO 
             END
            ) + 
         SUM(CASE WHEN SP_USUARIO.CARGOFIJO IN (0) AND FACTURADOCONCEPTO12.FINAN NOT IN (0) 
                  THEN FACTURADOCONCEPTO12.ACUEDUCTO 
                  ELSE 0
             END
            )
         ) - 
        (SUM(SP_USUARIO.SOBREFIJO) - SUM(SP_USUARIO.SUBFIJO  ))     AS TOTALACUEDUCTO,
        (SUM(CASE WHEN SP_USUARIO.CARGOFIJOAL IN (0) 
                  THEN 0 
                  ELSE SP_USUARIO.CARGOFIJOAL 
             END) +
         SUM(CASE WHEN SP_USUARIO.CARGOFIJOAL IN (0) AND FACTURADOCONCEPTO12.FINAN NOT IN (0) 
                  THEN FACTURADOCONCEPTO12.ALCANTARILLADO 
                  ELSE 0
             END )
         ) - 
        (SUM(SP_USUARIO.SOBREFIJOALC) - SUM(SP_USUARIO.SUBFIJOALC)) AS TOTALALCANTARILLADO
FROM FACTURADOCONCEPTO12 
  INNER JOIN SP_USUARIO 
     ON FACTURADOCONCEPTO12.COMPANIA   = SP_USUARIO.COMPANIA 
    AND FACTURADOCONCEPTO12.CODIGORUTA = SP_USUARIO.CODIGORUTA 
    AND FACTURADOCONCEPTO12.CICLO      = SP_USUARIO.CICLO 
  INNER JOIN SP_ESTRATOS 
    ON  SP_USUARIO.COMPANIA            = SP_ESTRATOS.COMPANIA 
    AND SP_USUARIO.ESTRATO             = SP_ESTRATOS.CODIGO  
    AND SP_USUARIO.USO                 = SP_ESTRATOS.USO 
  INNER JOIN SP_USOS 
     ON  SP_ESTRATOS.COMPANIA          = SP_USOS.COMPANIA 
    AND SP_ESTRATOS.USO                = SP_USOS.CODIGO 
WHERE SP_USUARIO.COMPANIA = s$compania$s 
  AND SP_USUARIO.CICLO      BETWEEN 's$cicloIncial$s'   AND 's$cicloFinal$s' 
  AND SP_USUARIO.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 
  AND SP_USUARIO.ALCANTARILLADO NOT IN (0) 
  AND (   SP_USUARIO.ESTADO    NOT IN ('R') 
       OR SP_USUARIO.ACUEDUCTO NOT IN (0)) 
GROUP BY SP_USUARIO.COMPANIA, 
  SP_ESTRATOS.NOMBRE, 
  SP_USOS.NOMBRE, 
  SP_USUARIO.ANO, 
  SP_USUARIO.PERIODO, 
  SP_USUARIO.ALCANTARILLADO, 
  SP_USUARIO.CICLO 
