SELECT  SP_TARIFAS.CARGOFIJO, 
        SP_TARIFAS.SUBFIJO, 
        SP_TARIFAS.SOBREFIJO, 
        SP_TARIFAS.CONSUMOBASICO, 
        (SP_TARIFAS.TARIFABASICO + DECODE(s$tarifa$s, -1, SP_TARIFAS.VALORTASAAMBIENTALAC, 0)) AS TARIFABASICO, 
        SP_TARIFAS.SUBBASICO, 
        SP_TARIFAS.SOBREBASICO, 
        SP_TARIFAS.CONSUMOCOMPLEMENTARIO, 
        (SP_TARIFAS.TARIFACOMPLEMENTARIO + DECODE(s$tarifa$s, -1, SP_TARIFAS.VALORTASAAMBIENTALAC, 0)) AS TARIFACOMPLEMENTARIO, 
        SP_TARIFAS.SUBCOMPLEMENTARIO, 
        SP_TARIFAS.SOBRECOMPLEMENTARIO, 
        SP_TARIFAS.CONSUMOSUNTUARIO, 
        (SP_TARIFAS.TARIFASUNTUARIO + DECODE(s$tarifa$s, -1, SP_TARIFAS.VALORTASAAMBIENTALAC, 0)) AS TARIFASUNTUARIO, 
        SP_TARIFAS.SUBSUNTUARIO, 
        SP_TARIFAS.SOBRESUNTUARIO, 
        SP_TARIFAS.DERCONEXIONACUE, 
        SP_TARIFAS.RECONEXION, 
        SP_TARIFAS.SINMEDICION, 
        SP_TARIFAS.USO, 
        SP_TARIFAS.ESTRATO, 
        SP_USOS.NOMBRE AS NOMBREUSO, 
        SP_ESTRATOS.NOMBRE AS NOMBREESTRATO 
FROM    SP_TARIFAS 
        INNER JOIN SP_USOS 
          ON  (SP_TARIFAS.COMPANIA  = SP_USOS.COMPANIA 
          AND SP_TARIFAS.USO        = SP_USOS.CODIGO) 
          INNER JOIN SP_ESTRATOS 
            ON (SP_TARIFAS.COMPANIA = SP_ESTRATOS.COMPANIA 
            AND SP_TARIFAS.USO      = SP_ESTRATOS.USO 
            AND SP_TARIFAS.ESTRATO  = SP_ESTRATOS.CODIGO) 
WHERE SP_TARIFAS.COMPANIA     = s$compania$s 
      AND SP_TARIFAS.ANO      = s$ano$s 
      AND SP_TARIFAS.PERIODO  = 's$mes$s' 
ORDER BY  SP_TARIFAS.COMPANIA, 
          SP_TARIFAS.ANO, 
          SP_TARIFAS.PERIODO, 
          SP_TARIFAS.USO, 
          SP_TARIFAS.ESTRATO
