SELECT SP_PLANOSDIGITAL.COMPANIA,
  SP_PLANOSDIGITAL.CICLO,
  SP_PLANOSDIGITAL.AFORADOR,
  CNSPLANOSFLIMITES.RUTAINICIAL,
  CNSPLANOSFLIMITES.RUTAFINAL,
  SP_PLANOSDIGITAL.MAECOD1 AS CODIGOINTERNO,
  SP_USUARIO.DIRTECNICA,
  SP_USUARIO.LECTURA,
  SP_PLANOSDIGITAL.MAELEC0 AS LECTURASITIO,
  TO_CHAR(SP_PLANOSDIGITAL.FECHASUBIDA,'DD/MM/YYYY') AS FECHASUBIDA,
  PCK_SERVICIOS_PUBLICOS_COM2.FC_GETFECHATEXTO(s$compania$s,MAEHORAL,'F') AS FECHA,
  PCK_SERVICIOS_PUBLICOS_COM2.FC_GETFECHATEXTO(s$compania$s,MAEHORAL,'H')AS HORA
FROM (SP_USUARIO
  INNER JOIN SP_PLANOSDIGITAL
    ON  (SP_USUARIO.COMPANIA    = SP_PLANOSDIGITAL.COMPANIA)
    AND (SP_USUARIO.CICLO      = SP_PLANOSDIGITAL.CICLO)
    AND (SP_USUARIO.ANO        = SP_PLANOSDIGITAL.ANO)
    AND (SP_USUARIO.PERIODO    = SP_PLANOSDIGITAL.PERIODO)
    AND (SP_USUARIO.CODIGORUTA = SP_PLANOSDIGITAL.MAECOD))
  INNER JOIN (SELECT SP_PLANOSDIGITAL.COMPANIA,
      SP_PLANOSDIGITAL.CICLO,
      SP_PLANOSDIGITAL.AFORADOR,
      MAX(SP_PLANOSDIGITAL.MAECOD) AS RUTAFINAL,
      MIN(SP_PLANOSDIGITAL.MAECOD) AS RUTAINICIAL
    FROM SP_USUARIO
      INNER JOIN SP_PLANOSDIGITAL
        ON  SP_USUARIO.COMPANIA  = SP_PLANOSDIGITAL.COMPANIA
        AND SP_USUARIO.CICLO = SP_PLANOSDIGITAL.CICLO
        AND SP_USUARIO.ANO  = SP_PLANOSDIGITAL.ANO
        AND SP_USUARIO.PERIODO = SP_PLANOSDIGITAL.PERIODO
        AND SP_USUARIO.CODIGORUTA = SP_PLANOSDIGITAL.MAECOD
    GROUP BY SP_PLANOSDIGITAL.COMPANIA,
      SP_PLANOSDIGITAL.CICLO,
      SP_PLANOSDIGITAL.AFORADOR)CNSPLANOSFLIMITES
    ON  SP_PLANOSDIGITAL.AFORADOR  = CNSPLANOSFLIMITES.AFORADOR
    AND SP_PLANOSDIGITAL.CICLO    = CNSPLANOSFLIMITES.CICLO
    AND SP_PLANOSDIGITAL.COMPANIA = CNSPLANOSFLIMITES.COMPANIA
WHERE SP_PLANOSDIGITAL.COMPANIA=s$compania$s
  AND SP_PLANOSDIGITAL.CICLO BETWEEN 's$cicloInicial$s' AND 's$cicloFinal$s'
  AND SP_PLANOSDIGITAL.FECHASUBIDA BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
  AND SP_PLANOSDIGITAL.MAECOD BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
  AND SP_PLANOSDIGITAL.AFORADOR = 's$aforador$s'
