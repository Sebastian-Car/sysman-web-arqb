SELECT SP_USUARIO.CODIGOINTERNO,
  SP_HISTORIA_EXTERNA.PERIODO,
  NVL(SP_HISTORIA_EXTERNA.VALORASEO,0) AS TERCERIZA,
  TO_CHAR(SP_HISTORIA_EXTERNA.FECHA_PAGO,'DD/MM/YYYY') AS FECHA_PAGO,
  SP_HISTORIA_EXTERNA.BANCO_PAGO,
  SP_USUARIO.NIT
FROM SP_USUARIO
INNER JOIN SP_HISTORIA_EXTERNA
ON SP_USUARIO.COMPANIA    = SP_HISTORIA_EXTERNA.COMPANIA
AND SP_USUARIO.CICLO      = SP_HISTORIA_EXTERNA.CICLO
AND SP_USUARIO.CODIGORUTA = SP_HISTORIA_EXTERNA.CODIGORUTA
WHERE  SP_USUARIO.COMPANIA='s$compania$s'
AND TRUNC(SP_HISTORIA_EXTERNA.FECHA_PAGO) BETWEEN s$fechaInicial$s AND s$fechaFinal$s
AND SP_HISTORIA_EXTERNA.FECHA_PAGO IS NOT NULL
ORDER BY SP_USUARIO.COMPANIA
