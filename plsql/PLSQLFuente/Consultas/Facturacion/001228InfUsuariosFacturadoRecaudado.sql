SELECT SP_USUARIO.ANO, 
  SP_USUARIO.PERIODO, 
  SP_USUARIO.CODIGORUTA, 
  SP_USUARIO.DIRTECNICA, 
  (SP_USUARIO.NOMBRES || ' ' || SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO)   AS NOMBRE, 
  SUM(SP_FACTURADO.VALOR_FACTURADO) AS FACTURADO, 
  NVL(SP_HISTORIA_EXTERNA.VALORASEO,0) AS TERCERIZADO, 
  (CASE WHEN (CASE WHEN SP_HISTORIA_EXTERNA.BANCO_PAGO IS NULL THEN 0 ELSE -1 END) NOT IN (0) THEN NVL(SP_HISTORIA_EXTERNA.VALORASEO,0) ELSE 0 END) AS TERCERIZADORECAUDADO, 
  NVL(SP_HISTORIA_CONVENIOS.TOTAL,0)  AS CONVENIOS, 
  (CASE WHEN (CASE WHEN SP_HISTORIA_CONVENIOS.BANCO_PAGO IS NULL THEN 0 ELSE -1 END) NOT IN (0) THEN NVL(SP_HISTORIA_CONVENIOS.TOTAL,0) ELSE 0 END) AS CONVENIOSRECAUDO, 
  (CASE WHEN NVL(SP_HISTORIA_EXTERNA.BANCO_PAGO,'') = '' THEN 0 ELSE -1 END) AS PAGOTER, 
  (CASE WHEN NVL(SP_HISTORIA_CONVENIOS.BANCO_PAGO,'') = '' THEN 0 ELSE -1 END) AS PAGOCONVE, 
  (CASE WHEN SP_FACTURADO.CONCEPTO IN(49) THEN SP_FACTURADO.VALOR_FACTURADO ELSE 0 END) AS ABONO, 
  (CASE WHEN NVL(SP_USUARIO.BANCOPERPROCESO,'') = '' THEN 0 ELSE -1 END) AS PAGO, 
  CASE WHEN NVL( SP_USUARIO.FECHAPAGOPERPROCESO, '') IN ('') THEN  CASE WHEN  NVL(SP_HISTORIA_EXTERNA.FECHA_PAGO,'') IN('') THEN NVL(SP_HISTORIA_CONVENIOS.FECHA_PAGO,'') ELSE NVL(SP_HISTORIA_EXTERNA.FECHA_PAGO,'') END ELSE NVL(SP_USUARIO.FECHAPAGOPERPROCESO,'') END AS FECHAPAGOPERPROCESO, 
  NVL(SP_USUARIO.TOTFACTURAPERACTUAL, 0) AS TOTFACTURAPERACTUAL,
  (CASE WHEN (CASE WHEN SP_USUARIO.BANCOPERPROCESO IS NULL THEN 0 ELSE -1 END) NOT IN (0) THEN SP_USUARIO.TOTFACTURAPERACTUAL ELSE 0 END) AS TOTFACTURAPERACTUALRECAUDADO, 
  SP_HISTORIA_EXTERNA.FECHA_PAGO   AS FECHATER, 
  SP_HISTORIA_CONVENIOS.FECHA_PAGO AS FECHACONVE, 
  SP_USUARIO.COMPANIA 
FROM SP_USUARIO 
INNER JOIN SP_FACTURADO 
  ON  SP_USUARIO.PERIODO     = SP_FACTURADO.PERIODO 
  AND SP_USUARIO.ANO        = SP_FACTURADO.ANO 
  AND SP_USUARIO.CODIGORUTA = SP_FACTURADO.CODIGORUTA 
  AND SP_USUARIO.CICLO      = SP_FACTURADO.CICLO 
  AND SP_USUARIO.COMPANIA   = SP_FACTURADO.COMPANIA 
LEFT JOIN SP_HISTORIA_EXTERNA 
  ON  SP_USUARIO.COMPANIA    = SP_HISTORIA_EXTERNA.COMPANIA 
  AND SP_USUARIO.CICLO     = SP_HISTORIA_EXTERNA.CICLO 
  AND SP_USUARIO.CODIGORUTA = SP_HISTORIA_EXTERNA.CODIGORUTA 
  AND SP_USUARIO.ANO        = SP_HISTORIA_EXTERNA.ANO 
  AND SP_USUARIO.PERIODO    = SP_HISTORIA_EXTERNA.PERIODO 
LEFT JOIN SP_HISTORIA_CONVENIOS 
  ON  SP_USUARIO.PERIODO     = SP_HISTORIA_CONVENIOS.PERIODO 
  AND SP_USUARIO.ANO        = SP_HISTORIA_CONVENIOS.ANO 
  AND SP_USUARIO.CODIGORUTA = SP_HISTORIA_CONVENIOS.CODIGORUTA 
  AND SP_USUARIO.CICLO      = SP_HISTORIA_CONVENIOS.CICLO 
  AND SP_USUARIO.COMPANIA   = SP_HISTORIA_CONVENIOS.COMPANIA 
WHERE SP_USUARIO.COMPANIA = s$compania$s
  AND SP_USUARIO.CICLO  
  BETWEEN  's$cicloInicial$s' AND 's$cicloFinal$s'
GROUP BY SP_USUARIO.ANO, 
  SP_USUARIO.PERIODO, 
  SP_USUARIO.CODIGORUTA, 
  SP_USUARIO.DIRTECNICA, 
  (SP_USUARIO.NOMBRES || ' ' || SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO),  
  NVL(SP_HISTORIA_EXTERNA.VALORASEO,0), 
  (CASE WHEN (CASE WHEN SP_HISTORIA_EXTERNA.BANCO_PAGO IS NULL THEN 0 ELSE -1 END) NOT IN (0) THEN NVL(SP_HISTORIA_EXTERNA.VALORASEO,0) ELSE 0 END), 
  NVL(SP_HISTORIA_CONVENIOS.TOTAL,0), 
  (CASE WHEN (CASE WHEN SP_HISTORIA_CONVENIOS.BANCO_PAGO IS NULL THEN 0 ELSE -1 END) NOT IN (0) THEN NVL(SP_HISTORIA_CONVENIOS.TOTAL,0) ELSE 0 END), 
  (CASE WHEN NVL(SP_HISTORIA_EXTERNA.BANCO_PAGO,'') = '' THEN 0 ELSE -1 END), 
  (CASE WHEN NVL(SP_HISTORIA_CONVENIOS.BANCO_PAGO,'') = '' THEN 0 ELSE -1 END), 
  (CASE WHEN SP_FACTURADO.CONCEPTO IN(49) THEN SP_FACTURADO.VALOR_FACTURADO ELSE 0 END), 
  (CASE WHEN NVL(SP_USUARIO.BANCOPERPROCESO,'') = '' THEN 0 ELSE -1 END), 
  CASE WHEN NVL( SP_USUARIO.FECHAPAGOPERPROCESO, '') IN ('') THEN  CASE WHEN  NVL(SP_HISTORIA_EXTERNA.FECHA_PAGO,'') IN('') THEN NVL(SP_HISTORIA_CONVENIOS.FECHA_PAGO,'') ELSE NVL(SP_HISTORIA_EXTERNA.FECHA_PAGO,'') END ELSE NVL(SP_USUARIO.FECHAPAGOPERPROCESO,'') END, 
  NVL(SP_USUARIO.TOTFACTURAPERACTUAL, 0),
  (CASE WHEN (CASE WHEN SP_USUARIO.BANCOPERPROCESO IS NULL THEN 0 ELSE -1 END) NOT IN (0) THEN SP_USUARIO.TOTFACTURAPERACTUAL ELSE 0 END), 
  SP_HISTORIA_EXTERNA.FECHA_PAGO, 
  SP_HISTORIA_CONVENIOS.FECHA_PAGO, 
  SP_USUARIO.COMPANIA
