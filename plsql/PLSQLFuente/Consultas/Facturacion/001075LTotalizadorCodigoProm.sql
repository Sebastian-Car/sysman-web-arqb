WITH PRIMERTOTALIZADOR AS(
    SELECT COMPANIA, 
        CICLO, 
        CODIGORUTA, 
        CONSUMO, 
        DIRTECNICA, 
        NVL(PRIMERAPELLIDO, ' ') || ' ' || NVL(SEGUNDOAPELLIDO, ' ') || ' ' || NVL(NOMBRES, ' ') AS NOMBRE,
        CODIGOINTERNO,
        USO,
        ESTRATO,
        LECTURA,
        LECTURA1,
        CONSUMOACU,
        CONSUMOALC,
        CONSUMOPROM,
        CONSUMOMANUAL,
        TOTALIZADOR,
        (CASE WHEN TOTALIZADOR <> 'T' 
            THEN LAG(CODIGORUTA) OVER(ORDER BY COMPANIA, CICLO, CODIGORUTA DESC)
        END) ANTERIOR
    FROM SP_USUARIO
    WHERE COMPANIA = s$compania$s
        AND CICLO = s$ciclo$s
        AND TOTALIZADOR IS NOT NULL
    ORDER BY COMPANIA, CICLO, CODIGORUTA DESC),
DATOSAREACOMUN AS(
  SELECT COMPANIA COMPANIADET, 
      CICLO CICLODET, 
      CODIGORUTA CODIGORUTADET, 
      (CASE WHEN TOTALIZADOR = 'S' 
        THEN(
          CASE WHEN LAG(ANTERIOR) OVER (ORDER BY COMPANIA, CICLO, CODIGORUTA DESC)IS NULL
            THEN ANTERIOR
            ELSE (
              SELECT MIN(CODIGORUTA)
              FROM PRIMERTOTALIZADOR
              WHERE PRIMERTOTALIZADOR.CODIGORUTA > SEGUNDO.CODIGORUTA
                AND PRIMERTOTALIZADOR.TOTALIZADOR = 'T')
        END)
      ELSE NULL
      END) AS CODIGOTOTALIZADOR,
      CONSUMO CONSUMODET, 
      DIRTECNICA DIRTECNICADET, 
      NOMBRE NOMBREDET,
      CODIGOINTERNO CODIGOINTERNODET,
      USO USODET,
      ESTRATO ESTRATODET,
      LECTURA LECTURAACTUALDET,
      LECTURA1 LECTURAANTERIORDET,
      CONSUMOACU CONSUMOACUDET,
      CONSUMOALC CONSUMOALCDET,
      CONSUMOPROM CONSUMOPROMDET,
      CONSUMOMANUAL CONSUMOMANUALDET,
      TOTALIZADOR TOTALIZADORDET
  FROM PRIMERTOTALIZADOR SEGUNDO),
DATOSTOTALIZADOR AS (
  SELECT U.CODIGORUTA,
    CODIGOINTERNO,
    CONSUMO,
    M.CODIGO MEDIDOR,
    US.NOMBRE NOMBREUSO,
    E.NOMBRE NOMBREESTRATO,
    NVL(PRIMERAPELLIDO, ' ') || ' ' || NVL(SEGUNDOAPELLIDO, ' ') || ' ' || NVL(NOMBRES, ' ') AS NOMBRE,
    DIRTECNICA,
    LECTURA LECTURAACTUAL,
    LECTURA1 LECTURAANTERIOR,
    CONSUMOACU,
    CONSUMOALC,
    CONSUMOPROM,
    CONSUMOMANUAL
  FROM SP_USUARIO U, SP_ESTRATOS E, SP_USOS US, SP_MEDIDOR M
  WHERE U.ESTRATO 	 = E.CODIGO
    AND U.USO     	 = E.USO
    AND U.USO     	 = US.CODIGO
    AND U.COMPANIA   = s$compania$s
    AND E.COMPANIA   = U.COMPANIA
    AND US.COMPANIA  = U.COMPANIA
    AND U.CODIGORUTA = M.CODIGORUTA)
SELECT DAC.*, 
  DT.*, 
  E.NOMBRE NOMBREESTRATODET,
  U.NOMBRE NOMBREUSODET,
  ME.CODIGO MEDIDORDET
FROM DATOSAREACOMUN DAC, DATOSTOTALIZADOR DT, SP_ESTRATOS E, SP_USOS U, SP_MEDIDOR ME
  WHERE CODIGOTOTALIZADOR = DT.CODIGORUTA
  AND ESTRATODET    = E.CODIGO
  AND USODET     	= E.USO  
  AND USODET     	= U.CODIGO
  AND U.COMPANIA 	= s$compania$s
  AND E.COMPANIA 	= U.COMPANIA
  AND CODIGOTOTALIZADOR IS NOT NULL
  AND ME.CODIGORUTA = DAC.CODIGORUTADET
