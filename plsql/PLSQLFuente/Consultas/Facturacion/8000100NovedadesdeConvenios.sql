SELECT 
  E.NIT,
  E.NOMBRE EMPRESA,
  TO_CHAR(H.ANO) ANO,
  H.PERIODO,
  H.CODIGO,
  H.BANCO_PAGO,
  TO_CHAR(H.FECHA_PAGO,'DD/MM/YYYY') FECHA_PAGO,
  H.FACTURA NFACTURA
FROM SP_HISTORIA_CONVENIOS H
INNER JOIN SP_EMPRESAS_CONVENIO E
ON H.COMPANIA    = E.COMPANIA
AND H.ID_EMPRESA = E.ID
WHERE H.COMPANIA = s$compania$s
AND H.CICLO = s$ciclo$s
AND H.ANO = s$anio$s 
AND H.PERIODO  = s$periodo$s
AND H.ID_EMPRESA = s$convenio$s
s$condicion$s
ORDER BY H.FECHA_PAGO
