SELECT  SP_USUARIO.COMPANIA,
        SP_USUARIO.NOMBRES || ' ' || SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO AS NOMBRE,
        SP_USUARIO.CICLO,
        SP_USUARIO.CODIGORUTA,
        SP_USUARIO.ESTRATO,
        SP_USUARIO.USO,
        SP_USUARIO.CODIGOINTERNO,
        TO_CHAR(SP_USUARIO_DESCU.PORC_DESCUENTO) || '%' AS PORCENTAJE,
        SP_ESTRATOS.NOMBRE                                   AS ESTRATO_NOM,
        SP_USOS.NOMBRE                                       AS USO_NOM,
        SUM(SP_USUARIO_DESCU.VALOR_FAC)                      AS VALOR,
        SUM(SP_USUARIO_DESCU.BASE)                           AS BASE
FROM SP_USUARIO
  INNER JOIN SP_USUARIO_DESCU
          ON (SP_USUARIO.COMPANIA   = SP_USUARIO_DESCU.COMPANIA
         AND  SP_USUARIO.CICLO      = SP_USUARIO_DESCU.CICLO
         AND  SP_USUARIO.CODIGORUTA = SP_USUARIO_DESCU.CODIGORUTA
         AND  SP_USUARIO.ANO        = SP_USUARIO_DESCU.ANOFAC
         AND  SP_USUARIO.PERIODO    = SP_USUARIO_DESCU.PERIODOFAC)
         LEFT JOIN SP_ESTRATOS
            ON (SP_USUARIO.COMPANIA = SP_ESTRATOS.COMPANIA
           AND SP_USUARIO.USO       = SP_ESTRATOS.USO
           AND SP_USUARIO.ESTRATO   = SP_ESTRATOS.CODIGO)
           LEFT JOIN SP_USOS
              ON (SP_USUARIO.COMPANIA = SP_USOS.COMPANIA
             AND SP_USUARIO.USO       = SP_USOS.CODIGO)
WHERE SP_USUARIO.COMPANIA  = s$compania$s
  s$condicionCiclo$s
  AND SP_USUARIO.CODIGORUTA BETWEEN 's$codIni$s' AND 's$codFin$s'
  AND SP_USUARIO_DESCU.PORPORCENTAJE = -1
GROUP BY  SP_USUARIO.COMPANIA,
          SP_USUARIO.NOMBRES,
          SP_USUARIO.PRIMERAPELLIDO,
          SP_USUARIO.SEGUNDOAPELLIDO,
          SP_USUARIO.CICLO,
          SP_USUARIO.CODIGORUTA,
          SP_USUARIO.ESTRATO,
          SP_USUARIO.USO,
          SP_USUARIO.CODIGOINTERNO,
         TO_CHAR(SP_USUARIO_DESCU.PORC_DESCUENTO) || '%',
          SP_ESTRATOS.NOMBRE,
          SP_USOS.NOMBRE
