SELECT  SP_USUARIO.CODIGORUTA,
        SP_USUARIO.USO,
        SP_USUARIO.ANO,
        PCK_SYSMAN_UTL.FC_NOMBRE_MES(SP_USUARIO.PERIODO) PERIODO,
        SP_USUARIO.ESTRATO,
        SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO ||' '|| SP_USUARIO.NOMBRES NOMBREUSUARIO,
        SP_USUARIO.DIRTECNICA,
        SP_USUARIO.PERIODOSATRASO,
        SP_USUARIO.LECTURA,
        SP_USUARIO.LECTURA1,
        SP_USUARIO.LECTURA2,
        SP_USUARIO.LECTURA3,
        SP_USUARIO.LECTURA4,
        SP_USUARIO.LECTURA5,
        SP_USUARIO.LECTURA6
FROM SP_USUARIO 
WHERE SP_USUARIO.COMPANIA = s$compania$s
  AND SP_USUARIO.CICLO BETWEEN s$cicloInicial$s AND s$cicloFinal$s
  AND SP_USUARIO.PERIODOSATRASO = s$periodoAtraso$s
  AND SP_USUARIO.BANCOPERPROCESO IS NULL 
  AND SP_USUARIO.ESTADO NOT IN('R','S')
  s$condicion$s
