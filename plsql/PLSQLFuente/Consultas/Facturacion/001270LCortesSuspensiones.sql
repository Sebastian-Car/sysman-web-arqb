SELECT USUARIO.CODIGORUTA,
  USUARIO.CODIGOINTERNO,
  PRIMERAPELLIDO ||' '|| SEGUNDOAPELLIDO ||' '|| NOMBRES NOMBRECOMPLETO,
  DIRTECNICA,
  DIRGUIA,
  LECTURA,
  PERIODOSATRASO,
  TOTFACTURAPERACTUAL,
  CASE WHEN SP_MEDIDOR.CODIGO IS NOT NULL THEN SP_MEDIDOR.CODIGO ELSE ' ' END MEDIDOR,
  CODIGOCATASTRAL,
  CASE WHEN CHAPETAS NOT IN (0) THEN 'x'
            ELSE ' '
  END CHAPETAS,
  EXCLUIRCARTERA,
  BANCOPERPROCESO,
  USUARIO.ESTADO,
  NIT
FROM SP_USUARIO USUARIO
  INNER JOIN SP_MEDIDOR
     ON USUARIO.COMPANIA = SP_MEDIDOR.COMPANIA
    AND USUARIO.MEDIDOR  = SP_MEDIDOR.CONSECUTIVO
