SELECT SP_TMP_USUARIOS_PESOASEO.COMPANIA
      ,SP_TMP_USUARIOS_PESOASEO.CICLO
      ,SP_TMP_USUARIOS_PESOASEO.CODIGORUTA
      ,SP_TMP_USUARIOS_PESOASEO.USO
      ,SP_TMP_USUARIOS_PESOASEO.ESTRATO
      ,SP_TMP_USUARIOS_PESOASEO.ANO
      ,SP_TMP_USUARIOS_PESOASEO.PERIODO
      ,SP_TMP_USUARIOS_PESOASEO.PESOASEO
      ,' '||SP_USUARIO.NOMBRES||' '||SP_USUARIO.PRIMERAPELLIDO||' '||SP_USUARIO.SEGUNDOAPELLIDO NOMBRES
      ,SP_USUARIO.ASEO
      ,SP_USUARIO.CODIGOINTERNO
FROM   SP_TMP_USUARIOS_PESOASEO
  INNER JOIN SP_USUARIO
     ON      SP_TMP_USUARIOS_PESOASEO.COMPANIA   = SP_USUARIO.COMPANIA
    AND      SP_TMP_USUARIOS_PESOASEO.CICLO      = SP_USUARIO.CICLO
    AND      SP_TMP_USUARIOS_PESOASEO.CODIGORUTA = SP_USUARIO.CODIGORUTA
WHERE  SP_TMP_USUARIOS_PESOASEO.COMPANIA = s$compania$s
  AND  SP_TMP_USUARIOS_PESOASEO.CICLO    = s$ciclo$s
  AND  SP_TMP_USUARIOS_PESOASEO.PESOASEO NOT IN (s$pesoaseo$s,0)
  AND  SP_USUARIO.ASEO                      NOT IN (0)
