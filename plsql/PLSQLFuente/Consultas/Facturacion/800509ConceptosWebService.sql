SELECT
	C.COMPANIA,
	C.ANO,
	C.TIPOCOBRO,
	T.NOMBRE AS NOMBRE_TOPO,
	C.CODIGO,
	C.NOMBRE AS NOMBRE_CONCEPTO,
	C.DESCUENTO,
	C.VALOR_BASE,
	C.UNIDAD,
	C.CUENTADEBITOBASE,
	C.CUENTACREDITOBASE,
	C.CUENTADEBITOBASE_AV,
	C.CUENTACREDITOBASE_AV,
	C.CUENTA_RECAUDO,
	C.ESTADO,
	C.CLASE_CONCEPTO,
	C.REFERENCIA,
	C.FUENTE_RECURSO,
	C.CENTRO_COSTO,
	C.AUXILIAR
FROM
	SF_CONCEPTOS C
INNER JOIN SF_TIPO_COBRO T
ON
	(C.COMPANIA = T.COMPANIA
		AND C.TIPOCOBRO = T.CODIGO
		AND C.ANO = T.ANO)
INNER JOIN PLAN_CONTABLE P
ON
	(C.COMPANIA = P.COMPANIA
		AND C.ANO = P.ANO
		AND C.CUENTADEBITOBASE = P.CODIGO)
INNER JOIN PLAN_CONTABLE P1
ON
	(C.COMPANIA = P1.COMPANIA
		AND C.ANO = P1.ANO
		AND C.CUENTACREDITOBASE = P1.CODIGO)
INNER JOIN PLAN_CONTABLE P2
ON
	(C.COMPANIA = P2.COMPANIA
		AND C.ANO = P2.ANO
		AND C.CUENTA_RECAUDO = P2.CODIGO)
INNER JOIN CENTRO_COSTO CC
ON
	(C.COMPANIA = CC.COMPANIA
		AND C.ANO = CC.ANO
		AND C.CENTRO_COSTO = CC.CODIGO)
INNER JOIN REFERENCIA R
ON
	(C.COMPANIA = R.COMPANIA
		AND C.ANO = R.ANO
		AND C.REFERENCIA = R.CODIGO)
INNER JOIN FUENTE_RECURSOS F
ON
	(C.COMPANIA = F.COMPANIA
		AND C.ANO = F.ANO
		AND C.FUENTE_RECURSO = F.CODIGO)
WHERE
	C.COMPANIA = s$compania$s
	AND C.ANO = s$ano$s
ORDER BY
	C.TIPOCOBRO,
	C.CODIGO