SELECT 
       SUM(SP_USUARIO.SUBFIJO) SUBFIJOACU,
       SUM(SP_USUARIO.SUBCONSUMOAC) SUBCONSUMOACU,
       SUM(SP_USUARIO.SUBSINMEDICION) SUBSINMEDICACU,
       SUM(SP_USUARIO.SUBACUEDUCTO) TOTALSUBACU, 
       SUM(SP_USUARIO.SUBFIJOALC) SUBFIJOALC,
       SUM(SP_USUARIO.SUBCONSUMOAL) SUBCONSUMOALC,
       SUM(SP_USUARIO.SUBALCSINMEDICION) SUBSINMEDIALC,
       SUM(SP_USUARIO.SUBALCANTARILLADO) TOTALSUBALC, 
       SUM(SP_USUARIO.SUBASEO) SUBSASEO, 
       SUM(SP_USUARIO.SUBBARR_LIM) SUBBARRIDOASEO,
       SUM(SP_USUARIO.SUBDISP_FINAL) SUBTRATAFINALASEO,
       SUM(SP_USUARIO.SUBMANEJO_REC) SUBCOMERASEO,
       SUM(SP_USUARIO.SUBRECOLECCION) SUBRECOLECASEO,
       SUM(SP_USUARIO.SUBTRAMO) SUBTEXECEASEO, 
       SUM(SP_USUARIO.SOBREFIJO) SOBFIJOACU,
       SUM(SP_USUARIO.SOBRECONSUMOAC) SOBCONSUMOACU,
       SUM(SP_USUARIO.SOBRESINMEDICION) SOBSMEDIACU,
       SUM(SP_USUARIO.SOBREACUEDUCTO) TOTALSOBACU, 
       SUM(SP_USUARIO.SOBREFIJOALC) SOBDIJOALCAN,
       SUM(SP_USUARIO.SOBRECONSUMOAL) SOBCONSUMOALC,
       SUM(SP_USUARIO.SOBREALCSINMEDICION) SOBSINMEDIALC,
       SUM(SP_USUARIO.SOBREALCANTARILLADO) TOTALSOBALC, 
       SUM(SP_USUARIO.SOBREASEO) SOBASEO,
       SUM(SP_USUARIO.SOBBARR_LIM) SOBBARRIDOASEO,
       SUM(SP_USUARIO.SOBDISP_FINAL) SOBTRATAFINALASEO,
       SUM(SP_USUARIO.SOBMANEJO_REC) SOBCOMERASEO,
       SUM(SP_USUARIO.SOBRECOLECCION) SOBRECOLECASEO,
       SUM(SP_USUARIO.SOBTRAMO) SOBTEXECEASEO, 
       SUM(SP_USUARIO.SUBSIDIO) TOTALSUBSIDIOS,
       SUM(SP_USUARIO.SOBREPRECIO) TOTALSOBRE 
  FROM SP_USUARIO 
    INNER JOIN SP_ESTRATOS 
       ON SP_USUARIO.ESTRATO = SP_ESTRATOS.CODIGO
      AND SP_USUARIO.USO = SP_ESTRATOS.USO
      AND SP_USUARIO.COMPANIA = SP_ESTRATOS.COMPANIA 
    INNER JOIN SP_USOS 
       ON SP_ESTRATOS.COMPANIA = SP_USOS.COMPANIA 
      AND SP_ESTRATOS.USO = SP_USOS.CODIGO 
 WHERE SP_USUARIO.COMPANIA = s$compania$s
   AND SP_USUARIO.CICLO BETWEEN s$cicloInicial$s AND s$cicloFinal$s
   AND SP_USUARIO.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 
   AND SP_USUARIO.ESTADO <> 'R' 
   AND SP_USOS.CODIGO ='s$uso$s'
   s$parSubi$s
ORDER BY SP_USUARIO.USO,SP_USUARIO.ESTRATO
