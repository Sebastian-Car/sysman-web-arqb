SELECT SP_USUARIO.COMPANIA, 
  SP_USUARIO.SECTHIDRAULICO,  
  SP_USUARIO.ANO, 
  SP_USUARIO.PERIODO, 
  SP_USUARIO.CICLO, 
  COUNT(SP_USUARIO.COMPANIA)  AS USUARIOS, 
  SP_USUARIO.BARRIO 
FROM SP_USUARIO 
WHERE SP_USUARIO.COMPANIA  = 's$compania$s' 
  AND SP_USUARIO.CICLO IN (s$ciclo$s) 
  AND SP_USUARIO.ESTADO NOT IN ('R','S') 
  AND SP_USUARIO.ACUEDUCTO <> 0  
GROUP BY SP_USUARIO.COMPANIA, 
  SP_USUARIO.SECTHIDRAULICO, 
  SP_USUARIO.ANO, 
  SP_USUARIO.PERIODO, 
  SP_USUARIO.CICLO, 
  SP_USUARIO.BARRIO
