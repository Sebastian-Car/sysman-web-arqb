SELECT 
  ABONOSPERIODO.COMPANIA, 
  ABONOSPERIODO.CICLO, 
  ABONOSPERIODO.CODIGORUTA, 
  ABONOSPERIODO.ANO, 
  ABONOSPERIODO.PERIODO, 
  ABONOSPERIODO.VALOR_FACTURADO, 
  SUM (VALORABONOACT + VALORABONOANT) SUMAAB
FROM (SELECT 
        SP_FACTURADO.COMPANIA, 
        SP_FACTURADO.CICLO, 
        SP_FACTURADO.CODIGORUTA, 
        SP_FACTURADO.ANO, 
        SP_FACTURADO.PERIODO, 
        SP_FACTURADO.CONCEPTO, 
        SP_FACTURADO.VALOR_FACTURADO 
      FROM SP_CICLO 
      INNER JOIN SP_FACTURADO 
         ON SP_CICLO.PERIODO = SP_FACTURADO.PERIODO 
        AND SP_CICLO.ANO = SP_FACTURADO.ANO  
        AND SP_CICLO.NUMERO = SP_FACTURADO.CICLO  
        AND SP_CICLO.COMPANIA = SP_FACTURADO.COMPANIA 
      WHERE SP_CICLO.COMPANIA=s$compania$s 
        AND SP_FACTURADO.CONCEPTO=49
        AND SP_FACTURADO.VALOR_FACTURADO <> 0 
      ) ABONOSPERIODO 
  INNER JOIN SP_FACTURADO 
     ON ABONOSPERIODO.PERIODO = SP_FACTURADO.PERIODO  
    AND ABONOSPERIODO.ANO = SP_FACTURADO.ANO 
    AND ABONOSPERIODO.CODIGORUTA = SP_FACTURADO.CODIGORUTA 
    AND ABONOSPERIODO.CICLO = SP_FACTURADO.CICLO 
    AND ABONOSPERIODO.COMPANIA = SP_FACTURADO.COMPANIA 
WHERE ABONOSPERIODO.COMPANIA=s$compania$s  
  AND (SP_FACTURADO.CONCEPTO BETWEEN 1 AND 48 OR SP_FACTURADO.CONCEPTO IN (246,247,248,249)) 
  AND SP_FACTURADO.CONCEPTO NOT IN (12,17) 
GROUP BY ABONOSPERIODO.COMPANIA, ABONOSPERIODO.CICLO, ABONOSPERIODO.CODIGORUTA, ABONOSPERIODO.ANO, ABONOSPERIODO.PERIODO, ABONOSPERIODO.VALOR_FACTURADO
HAVING (SUM (VALORABONOACT + VALORABONOANT)) <> -(ABONOSPERIODO.VALOR_FACTURADO)
