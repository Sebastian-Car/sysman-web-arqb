SELECT SP_USUARIO.COMPANIA,
       SP_USUARIO.CICLO,
       SP_USUARIO.CODIGORUTA,
       SP_USUARIO.ANO,
       SP_USUARIO.PERIODO,
       (SP_USUARIO.PRIMERAPELLIDO||' '||SP_USUARIO.SEGUNDOAPELLIDO||' '||SP_USUARIO.NOMBRES) NOMBRE,
       SP_USUARIO.CODIGOINTERNO, 
       SP_USUARIO.DIRTECNICA,
       SP_USUARIO.DIRGUIA,
       SP_USUARIO.USO,
       SP_USUARIO.LECTURA1,
       SP_USUARIO.LECTURA,
       SP_USUARIO.LECTURAAFORO,
       SP_USUARIO.CONSUMO,
       SP_USUARIO.CONSUMOPROM,
       CASE WHEN LECTURAAFORO=0 AND LECTURA=0 THEN 'Sin lectura' ELSE CASE WHEN LECTURA-LECTURA1<0 THEN 'Medidor invertido' ELSE CASE WHEN LECTURA-LECTURA1=0 THEN 'Medidor parado' ELSE CASE WHEN LECTURA-LECTURA1>10000 THEN 'Consumo elevado' ELSE 'Normal' END END END END OBSERVACIONES,          
       SP_USUARIO.PROBLEMALECTURA INDPROBLEMA,
       LECTURA-LECTURA1 TOTALCONSUMO,
       SP_USUARIO.MEDIDOR,
       SP_USUARIO.ESTRATO 
FROM SP_USUARIO
WHERE SP_USUARIO.COMPANIA = s$compania$s
  s$filtroCiclo$s
s$filtroIndProblema$s
  AND CASE WHEN LECTURAAFORO=0 AND LECTURA=0 THEN 'Sin lectura' ELSE CASE WHEN LECTURA-LECTURA1<0 THEN 'Medidor invertido' ELSE CASE WHEN LECTURA-LECTURA1=0 THEN 'Medidor parado' ELSE CASE WHEN LECTURA-LECTURA1>10000 THEN 'Consumo elevado' ELSE 'Normal' END END END END <> 'Normal'
ORDER BY SP_USUARIO.COMPANIA, SP_USUARIO.CICLO, SP_USUARIO.CODIGORUTA
