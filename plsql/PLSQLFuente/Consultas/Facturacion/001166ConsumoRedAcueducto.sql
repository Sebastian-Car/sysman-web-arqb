SELECT SP_USUARIO.COMPANIA, 
  SP_USUARIO.REDHIDRAULICA, 
  SP_USUARIO.ANO, 
  SP_USUARIO.PERIODO, 
  SP_USUARIO.CICLO, 
  COUNT(SP_USUARIO.COMPANIA) AS USUARIOS, 
  SUM(SP_USUARIO.CONSUMO) AS SUMADECONSUMO, 
  SUM(CASE WHEN TIPOCALCULO IN ('D','P') THEN CONSUMOPROM ELSE 0 END ) AS XCONSUMOPROMEDIO, 
  SP_REDESACUEDUCTO.NOMBRE  
FROM SP_USUARIO 
LEFT JOIN SP_REDESACUEDUCTO 
  ON  (SP_USUARIO.REDHIDRAULICA  = SP_REDESACUEDUCTO.CODIGO) 
  AND (SP_USUARIO.COMPANIA      = SP_REDESACUEDUCTO.COMPANIA) 
WHERE SP_USUARIO.COMPANIA ='s$compania$s' 
  AND SP_USUARIO.CICLO IN (s$ciclo$s) 
  AND SP_USUARIO.ESTADO NOT IN ('R','S') 
  AND SP_USUARIO.ACUEDUCTO  <> 0  
GROUP BY SP_USUARIO.COMPANIA, 
  SP_USUARIO.REDHIDRAULICA,  
  SP_USUARIO.ANO, 
  SP_USUARIO.PERIODO, 
  SP_USUARIO.CICLO, 
  SP_REDESACUEDUCTO.NOMBRE
