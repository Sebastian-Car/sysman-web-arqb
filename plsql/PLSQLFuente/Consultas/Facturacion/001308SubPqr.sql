SELECT COMPANIA, PROBLEMA, CLASEPROBLEMA, NOMBREPROBLEMA, Count(CLASEPROBLEMA)  TOTALPROBLEMAS,
Sum(CASE WHEN INDFAVOREMPRESA IS NULL THEN 0 ELSE 1 END) EMPRESA,
Sum(CASE WHEN FECHASOLUCION IS NULL THEN 0 ELSE 1 END )- Sum(CASE WHEN INDFAVOREMPRESA IS NULL THEN 0 ELSE 1 END)  USUARIO,
Sum(CASE WHEN FECHASOLUCION IS NULL THEN 0 ELSE 1 END ) SOLUCION,
Count(CLASEPROBLEMA)-Sum(CASE WHEN FECHASOLUCION IS NULL THEN 0 ELSE 1 END ) PENDIENTE,
Sum(DIASSOLUCION) DIASSOLUCION
,
TIPOREQUERIMIENTO
,
CASE WHEN TIPOREQUERIMIENTO = 'C' THEN 'Consulta'
     WHEN TIPOREQUERIMIENTO = 'Q' THEN 'Queja'
     WHEN TIPOREQUERIMIENTO = 'RE' THEN 'Reclamo'
     WHEN TIPOREQUERIMIENTO = 'R' THEN 'Recurso de Reposición'
     WHEN TIPOREQUERIMIENTO = 'P' THEN 'Petición'
     WHEN TIPOREQUERIMIENTO = 'RA' THEN 'Recurso de Reposición  y  Subsidiario de Apelación'
END TIPOREQUERIMIENTO1
FROM(
s$informeBase$s)
GROUP BY COMPANIA, PROBLEMA, CLASEPROBLEMA, NOMBREPROBLEMA
,TIPOREQUERIMIENTO
,CASE WHEN TIPOREQUERIMIENTO = 'C' THEN 'Consulta'
     WHEN TIPOREQUERIMIENTO = 'Q' THEN 'Queja'
     WHEN TIPOREQUERIMIENTO = 'RE' THEN 'Reclamo'
     WHEN TIPOREQUERIMIENTO = 'R' THEN 'Recurso de Reposición'
     WHEN TIPOREQUERIMIENTO = 'P' THEN 'Petición'
     WHEN TIPOREQUERIMIENTO = 'RA' THEN 'Recurso de Reposición  y  Subsidiario de Apelación'
END
ORDER BY TIPOREQUERIMIENTO,TO_NUMBER(PROBLEMA) ASC
