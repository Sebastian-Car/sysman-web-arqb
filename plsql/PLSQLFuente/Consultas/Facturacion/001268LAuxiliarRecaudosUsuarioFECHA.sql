SELECT 
SP_USUARIO.COMPANIA, 
  SP_USUARIO.CICLO, 
  SP_USUARIO.CODIGORUTA, 
 SP_USUARIO.FECHAPAGOPERPROCESO, 
  SP_PAGO.HORA, 
  SP_BANCOS.NOMBRE, 
  SP_USUARIO.BANCOPERPROCESO, 
  SP_USUARIO.PAQUETEPAGOPERPROCESO, 
  SP_FACTURADO.VALOR_FACTURADO, 
  SP_FACTURADO.DEUDA, 
  SP_FACTURADO.DOBLEPAGO, 
  (SP_FACTURADO.VALOR_FACTURADO+SP_FACTURADO.DEUDA+SP_FACTURADO.DOBLEPAGO) AS TOTALRECAUDADO, 
  (SP_USUARIO.BANCOPERPROCESO || PAQUETEPAGOPERPROCESO ) AS BANCOYPAQUETE, 
  SP_FACTURADO.CONCEPTO 
FROM SP_USUARIO 
  INNER JOIN SP_FACTURADO 
    ON  SP_USUARIO.COMPANIA  = SP_FACTURADO.COMPANIA 
    AND SP_USUARIO.CICLO     = SP_FACTURADO.CICLO 
    AND SP_USUARIO.CODIGORUTA = SP_FACTURADO.CODIGORUTA 
    INNER JOIN SP_PAGO 
    ON  SP_USUARIO.COMPANIA = SP_PAGO.COMPANIA 
    AND SP_USUARIO.CICLO = SP_PAGO.CICLO 
    AND SP_USUARIO.CODIGORUTA = SP_PAGO.CODIGORUTA 
    AND SP_USUARIO.CODIGOINTERNO = SP_PAGO.CODIGOINTERNO
    AND SP_USUARIO.PERIODO = SP_PAGO.PERIODO
    AND SP_USUARIO.ANO = SP_PAGO.ANO
  INNER JOIN SP_BANCOS 
    ON   SP_USUARIO.COMPANIA = SP_BANCOS.COMPANIA
    AND SP_USUARIO.BANCOPERPROCESO = SP_BANCOS.CODIGO 
WHERE SP_USUARIO.COMPANIA   = s$compania$s  
  AND SP_USUARIO.CICLO BETWEEN s$cicloInicial$s AND s$cicloFinal$s 
   AND SP_USUARIO.BANCOPERPROCESO BETWEEN 's$bancoInicial$s' AND 's$bancoFinal$s'
  AND (SP_FACTURADO.CONCEPTO BETWEEN 1 AND 49  
  OR  SP_FACTURADO.CONCEPTO  IN (246,247,248,249)) 
  AND FECHAPAGOPERPROCESO BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
ORDER BY SP_USUARIO.CODIGORUTA
