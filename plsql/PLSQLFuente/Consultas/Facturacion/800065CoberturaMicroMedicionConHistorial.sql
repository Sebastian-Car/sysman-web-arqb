SELECT SP_HISTORICOFACTURA.COMPANIA,
  SP_HISTORICOFACTURA.ANO,
  SP_HISTORICOFACTURA.PERIODO,
  SP_HISTORICOFACTURA.USO,
  SP_HISTORICOFACTURA.ESTRATO,
  COUNT(SP_HISTORICOFACTURA.ACUEDUCTO) AS ACTIVOS,
  SUM(CASE WHEN NVL(SP_HISTORICOFACTURA.MEDIDOR,0)<>'0'
           THEN 1
           ELSE 0
            END ) AS CONMEDIDOR,
  SUM( CASE WHEN NVL(SP_HISTORICOFACTURA.MEDIDOR,0) ='0'
            THEN 1
            ELSE 0
            END ) AS SINMEDIDOR 
   s$complementoHistorico$s
  
FROM SP_HISTORICOFACTURA
WHERE SP_HISTORICOFACTURA.COMPANIA='001'
s$variableCiclo$s 
s$variableHistoico$s
AND SP_HISTORICOFACTURA.ACUEDUCTO= -1
GROUP BY SP_HISTORICOFACTURA.COMPANIA,
  SP_HISTORICOFACTURA.ANO,
  SP_HISTORICOFACTURA.PERIODO ,
  SP_HISTORICOFACTURA.USO,
  SP_HISTORICOFACTURA.ESTRATO 
s$complementoHistorico$s
