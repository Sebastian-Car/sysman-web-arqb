SELECT SP_ORDENTRABAJO.COMPANIA
,SP_ORDENTRABAJO.CLASEDOC
,SP_ORDENTRABAJO.CICLO
,SP_ORDENTRABAJO.CODIGORUTA
,CASE WHEN SP_ORDENTRABAJO.TIPOREQUERIMIENTO = 'C' THEN 'Consulta'
     WHEN SP_ORDENTRABAJO.TIPOREQUERIMIENTO = 'Q' THEN 'Queja'
     WHEN SP_ORDENTRABAJO.TIPOREQUERIMIENTO = 'RE' THEN 'Reclamo'
     WHEN SP_ORDENTRABAJO.TIPOREQUERIMIENTO = 'R' THEN 'Recurso de Reposición'
     WHEN SP_ORDENTRABAJO.TIPOREQUERIMIENTO = 'P' THEN 'Petición'
     WHEN SP_ORDENTRABAJO.TIPOREQUERIMIENTO = 'RA' THEN 'Recurso de Reposición  y  Subsidiario de Apelación'
END TIPOREQUERIMIENTO
, CASE WHEN SP_ORDENTRABAJO.PRESENTACION ='V' THEN  'Verbal'
       WHEN SP_ORDENTRABAJO.PRESENTACION ='E' THEN  'Escrita'
       WHEN SP_ORDENTRABAJO.PRESENTACION ='T' THEN  'Teléfono'
       WHEN SP_ORDENTRABAJO.PRESENTACION ='C' THEN  'Correo'
END PRESENTACION
,(CASE WHEN SP_ORDENTRABAJO.ENVIOOFICINA = 0 THEN '' ELSE 'X' END) ENVIOOFICINA
,(CASE WHEN SP_ORDENTRABAJO.NECESITAVISITA = 0 THEN '' ELSE 'X' END) NECESITAVISITA
,SP_ORDENTRABAJO.DEPENDENCIAENV
,SP_ORDENTRABAJO.RECIBIDOPOR
,SP_ORDENTRABAJO.OPERADOR
,SP_ORDENTRABAJO.AFORADOR
,NVL(SP_ORDENTRABAJO.PRIMERAPELLIDO,' ') 
PRIMERAPELLIDO
,NVL(SP_ORDENTRABAJO.SEGUNDOAPELLIDO,' ')
 SEGUNDOAPELLIDO
,NVL(SP_ORDENTRABAJO.NOMBRES
,' ') NOMBRES
,SP_ORDENTRABAJO.FECHAENVIO
,SP_ORDENTRABAJO.FECHASOLICITUD
,SP_ORDENTRABAJO.HORASOLICITUD
,SP_D_ORDENTRABAJO.NUMERO
,SP_CLASEPROBLEMA.NOMBRE CLASEPROBLEMA
,SP_D_ORDENTRABAJO.PROBLEMA PROBLEMA
,SP_PROBLEMA.NOMBRE NOMBREPROBLEMA
,SP_D_ORDENTRABAJO.SOLUCION
,SP_D_ORDENTRABAJO.OBSERVACIONES
,SP_D_ORDENTRABAJO.FECHASOLUCION
,(CASE WHEN SP_D_ORDENTRABAJO.INDFAVOREMPRESA = 0 THEN '' ELSE 'X' END)INDFAVOREMPRESA, SP_D_ORDENTRABAJO.VRFACTURADOANT
,SP_D_ORDENTRABAJO.NUEVOVALOR
,SP_D_ORDENTRABAJO.TOTALFACTURAANT
,SP_D_ORDENTRABAJO.TOTFACTURADO
,SP_D_ORDENTRABAJO.CONCEPTO
,(LPAD(SP_D_ORDENTRABAJO.CLASEPROBLEMA,3)|| SP_D_ORDENTRABAJO.PROBLEMA) EXPR1
FROM SP_OrdenTrabajo INNER JOIN SP_D_OrdenTrabajo LEFT JOIN SP_ClaseProblema ON SP_D_OrdenTrabajo.CLASEPROBLEMA = SP_ClaseProblema.CLASEPROBLEMA AND SP_D_OrdenTrabajo.COMPANIA = SP_ClaseProblema.COMPANIA
LEFT JOIN SP_Problema ON SP_D_OrdenTrabajo.PROBLEMA = SP_Problema.CODIGO AND SP_D_OrdenTrabajo.CLASEPROBLEMA = SP_Problema.CLASEPROBLEMA AND SP_D_OrdenTrabajo.COMPANIA = SP_Problema.COMPANIA ON SP_OrdenTrabajo.NUMORDEN = SP_D_OrdenTrabajo.ORDENTRABAJO
AND SP_OrdenTrabajo.CLASEDOC = SP_D_OrdenTrabajo.CLASEDOC AND SP_OrdenTrabajo.COMPANIA = SP_D_OrdenTrabajo.COMPANIA
WHERE LPAD(SP_D_ORDENTRABAJO.CLASEPROBLEMA,3)|| SP_D_ORDENTRABAJO.PROBLEMA 
BETWEEN LPAD('s$claseProblemaInicial$s',3) || 's$problemaInicial$s'
AND LPAD('s$claseProblemaFinal$s',3) || 's$problemaFinal$s' AND SP_ORDENTRABAJO.COMPANIA= s$compania$s
 s$ciclo$s s$presentacion$s
s$claseDoc$s
AND SP_ORDENTRABAJO.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
AND SP_ORDENTRABAJO.FECHASOLICITUD BETWEEN TO_DATE('s$fechaInicial$s') AND TO_DATE('s$fechaFinal$s')
ORDER BY SP_ORDENTRABAJO.COMPANIA, SP_ORDENTRABAJO.CLASEDOC, SP_ORDENTRABAJO.NUMORDEN
