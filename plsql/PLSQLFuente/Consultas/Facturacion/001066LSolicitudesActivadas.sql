SELECT 
  SP_SOLICITUDSERVICIO.NUMERO,
  SP_SOLICITUDPORSERVICIO.NOMBRE  TIPODEMATRICULA,
  SP_SOLICITUDSERVICIO.NOMBRE,
  SP_SOLICITUDSERVICIO.DIRTECNICA,
  SP_SOLICITUDSERVICIO.TELEFONO,
  SP_SOLICITUDSERVICIO.FECHASOLICITUD
FROM SP_SOLICITUDPORSERVICIO
  INNER JOIN SP_SOLICITUDSERVICIO
    ON SP_SOLICITUDPORSERVICIO.COMPANIA  = SP_SOLICITUDSERVICIO.COMPANIA 
    AND SP_SOLICITUDPORSERVICIO.CODIGO  = SP_SOLICITUDSERVICIO.CLASESOLICITUD
WHERE SP_SOLICITUDSERVICIO.COMPANIA = s$compania$s
  AND SP_SOLICITUDSERVICIO.FECHASOLICITUD BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  s$condicion$s
   s$estado$s
ORDER BY  SP_SOLICITUDSERVICIO.CLASESOLICITUD
