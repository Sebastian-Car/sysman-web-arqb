SELECT USUARIO.CICLO,
  USUARIO.CODIGORUTA,
  USUARIO.CODIGOINTERNO, 
  USUARIO.ANO, 
  USUARIO.PERIODO, 
  USUARIO.PRIMERAPELLIDO || ' ' || USUARIO.SEGUNDOAPELLIDO || ' ' || USUARIO.NOMBRES  AS NOMBRE, 
  USOS.NOMBRE AS USO, 
  USUARIO.ESTRATO, 
  USUARIO.CONSUMOPROM 
FROM (SP_USUARIO USUARIO 
        INNER JOIN SP_USOS USOS 
           ON USUARIO.USO 		= USOS.CODIGO 
          AND USUARIO.COMPANIA 	= USOS.COMPANIA)
INNER JOIN SP_ESTRATOS ESTRATOS 
   ON USUARIO.ESTRATO 	= ESTRATOS.CODIGO
  AND USUARIO.USO 		= ESTRATOS.USO
  AND USUARIO.COMPANIA 	= ESTRATOS.COMPANIA
WHERE USUARIO.COMPANIA 	= s$compania$s 
  AND s$condicionUsuario$s USUARIO.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 
  AND USUARIO.ANO 		= s$anio$s 
  AND USUARIO.PERIODO 	= 's$periodo$s'

UNION
SELECT HISTORICOFACTURA.CICLO,
  HISTORICOFACTURA.CODIGORUTA,
  USUARIO.CODIGOINTERNO, 
  HISTORICOFACTURA.ANO, 
  HISTORICOFACTURA.PERIODO, 
  USUARIO.PRIMERAPELLIDO || ' ' || USUARIO.SEGUNDOAPELLIDO || ' ' || USUARIO.NOMBRES  AS NOMBRE, 
  USOS.NOMBRE AS USO, 
  HISTORICOFACTURA.ESTRATO, 
  HISTORICOFACTURA.CONSUMOPROM 
FROM (SP_HISTORICOFACTURA HISTORICOFACTURA 
        INNER JOIN SP_USUARIO USUARIO 
           ON HISTORICOFACTURA.CODIGORUTA 	= USUARIO.CODIGORUTA
          AND HISTORICOFACTURA.CICLO 		= USUARIO.CICLO
          AND HISTORICOFACTURA.COMPANIA 	= USUARIO.COMPANIA
		INNER JOIN SP_USOS USOS 
		   ON USUARIO.USO 					= USOS.CODIGO 
		  AND USUARIO.COMPANIA 				= USOS.COMPANIA)
INNER JOIN SP_ESTRATOS ESTRATOS 
   ON USUARIO.ESTRATO 		= ESTRATOS.CODIGO 
  AND USUARIO.USO 			= ESTRATOS.USO 
  AND USUARIO.COMPANIA 		= ESTRATOS.COMPANIA
WHERE HISTORICOFACTURA.COMPANIA 	= s$compania$s 
  AND s$condicionHistorico$s HISTORICOFACTURA.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 
  AND HISTORICOFACTURA.ANO  		= s$anio$s
  AND HISTORICOFACTURA.PERIODO 		= 's$periodo$s'
