SELECT 
   USUARIO.COMPANIA
  ,USUARIO.CICLO
  ,USUARIO.CODIGORUTA
  ,USUARIO.ANO
  ,USUARIO.PERIODO
  ,NVL(USUARIO.PRIMERAPELLIDO ,' ') PRIMERAPELLIDO
  ,NVL(USUARIO.SEGUNDOAPELLIDO,' ') SEGUNDOAPELLIDO
  ,NVL(USUARIO.NOMBRES        ,' ') NOMBRES
  ,USUARIO.CODIGOINTERNO
  ,USUARIO.DIRTECNICA
  ,USUARIO.LECTURA1
  ,USUARIO.LECTURA
  ,USUARIO.LECTURAAFORO
  ,USUARIO.CONSUMOPROM
  ,USUARIO.MEDIDOR
  ,USUARIO.LECTURA2
  ,USUARIO.LECTURACRITICA
  ,USUARIO.ESTADO
  ,NOMBREPROBLEMASUSUARIO.NOMBRE
  ,(USUARIO.LECTURAAFORO - USUARIO.LECTURA) TOTALCONSUMO
  ,NVL(USUARIO.PRIMERAPELLIDO ,' ') ||' '|| 
   NVL(USUARIO.SEGUNDOAPELLIDO,' ') ||' '||
   NVL(USUARIO.NOMBRES        ,' ') NOMBRECOMPLETO
  ,CASE WHEN USUARIO.ACUEDUCTO      NOT IN(0) THEN 'X' ELSE ' ' END INDACUEDUCTO
  ,CASE WHEN USUARIO.ALCANTARILLADO NOT IN(0) THEN 'X' ELSE ' ' END INDALCANTARILLADO
  ,CASE WHEN USUARIO.ASEO           NOT IN(0) THEN 'X' ELSE ' ' END INDASEO
  ,CASE WHEN USUARIO.INDDESHABITADO NOT IN(0) THEN 'X' ELSE ' ' END INDDESHABITADO
  ,CASE WHEN USUARIO.CHAPETAS       NOT IN(0) THEN 'X' ELSE ' ' END CHAPETAS
  ,CASE WHEN USUARIO.LECTURAAFORO IN(0) AND USUARIO.LECTURA IN(0)
        THEN 'SIN LECTURA'
        WHEN USUARIO.LECTURAAFORO NOT IN(0) AND USUARIO.LECTURA IN(0) AND USUARIO.CONSUMOPROM IN(0)
        THEN 'CONSUMO ELEVADO'
        WHEN (USUARIO.LECTURAAFORO - USUARIO.LECTURA) < 0
        THEN 'MEDIDOR INVERTIDO'
        WHEN (USUARIO.LECTURAAFORO - USUARIO.LECTURA) IN(0)
        THEN 'MEDIDOR PARADO'
        WHEN (USUARIO.LECTURAAFORO - USUARIO.LECTURA) >= USUARIO.CONSUMOPROM * (1+(s$limiteSuperior$s/100))
        THEN 'CONSUMO ELEVADO'
        WHEN (USUARIO.LECTURAAFORO - USUARIO.LECTURA) <= USUARIO.CONSUMOPROM * (1-(s$limiteInferior$s/100))
        THEN 'CONSUMO BAJO'
        ELSE 'NORMAL'
   END OBSERVACIONES
FROM SP_USUARIO USUARIO
  LEFT JOIN (
    SELECT 
       PROBLEMA.NOMBRE
      ,USUARIO_PROBLEMA.COMPANIA
      ,USUARIO_PROBLEMA.ANO
      ,USUARIO_PROBLEMA.PERIODO
      ,USUARIO_PROBLEMA.CICLO
      ,USUARIO_PROBLEMA.CODIGORUTA
      ,USUARIO_PROBLEMA.PROBLEMA
    FROM SP_PROBLEMA PROBLEMA
      INNER JOIN SP_USUARIO_PROBLEMA USUARIO_PROBLEMA
         ON PROBLEMA.COMPANIA      = USUARIO_PROBLEMA.COMPANIA
        AND PROBLEMA.CLASEPROBLEMA = USUARIO_PROBLEMA.CLASE
        AND PROBLEMA.CODIGO        = USUARIO_PROBLEMA.PROBLEMA
    WHERE USUARIO_PROBLEMA.COMPANIA = s$compania$s
  ) NOMBREPROBLEMASUSUARIO
     ON USUARIO.COMPANIA   = NOMBREPROBLEMASUSUARIO.COMPANIA
    AND USUARIO.CICLO      = NOMBREPROBLEMASUSUARIO.CICLO
    AND USUARIO.CODIGORUTA = NOMBREPROBLEMASUSUARIO.CODIGORUTA
    AND USUARIO.ANO        = NOMBREPROBLEMASUSUARIO.ANO
    AND USUARIO.PERIODO    = NOMBREPROBLEMASUSUARIO.PERIODO
WHERE USUARIO.COMPANIA  = s$compania$s
AND USUARIO.CICLO       = s$ciclo$s
AND USUARIO.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 
AND USUARIO.PERIODO     = s$periodo$s
AND NVL(CONSUMOPROM,0) >= s$valorProm$s
s$condicion$s
AND CASE WHEN USUARIO.LECTURAAFORO IN(0) AND USUARIO.LECTURA IN(0)
         THEN 'SIN LECTURA'
         WHEN USUARIO.LECTURAAFORO NOT IN(0) AND USUARIO.LECTURA IN(0) AND USUARIO.CONSUMOPROM IN(0)
         THEN 'CONSUMO ELEVADO'
         WHEN (USUARIO.LECTURAAFORO - USUARIO.LECTURA) < 0
         THEN 'MEDIDOR INVERTIDO'
         WHEN (USUARIO.LECTURAAFORO - USUARIO.LECTURA) IN(0)
         THEN 'MEDIDOR PARADO'
         WHEN (USUARIO.LECTURAAFORO - USUARIO.LECTURA) >= USUARIO.CONSUMOPROM * (1+(s$limiteSuperior$s/100))
         THEN 'CONSUMO ELEVADO'
         WHEN (USUARIO.LECTURAAFORO - USUARIO.LECTURA) <= USUARIO.CONSUMOPROM * (1-(s$limiteInferior$s/100))
         THEN 'CONSUMO BAJO'
         ELSE 'NORMAL'
    END NOT IN ('NORMAL')
ORDER BY 
  USUARIO.CODIGORUTA
