MERGE INTO CONSULTAS FIN USING (SELECT '800498NotasNoEmitidasDian' INFORME ,TO_CLOB(q'[SELECT
	COMPROBANTE_CNT.NUMERO,
	TO_CHAR(COMPROBANTE_CNT.FECHA, 'DD/MM/YYYY') FECHA,
	COMPROBANTE_CNT.TERCERO,
	COMPROBANTE_CNT.VLR_DOCUMENTO
FROM
	COMPROBANTE_CNT
INNER JOIN TERCERO ON
	COMPROBANTE_CNT.COMPANIA = TERCERO.COMPANIA
	AND COMPROBANTE_CNT.TERCERO = TERCERO.NIT
	AND COMPROBANTE_CNT.SUCURSAL = TERCERO.SUCURSAL
WHERE
	COMPROBANTE_CNT.COMPANIA = s$compania$s
	AND COMPROBANTE_CNT.FECHA BETWEEN 's$fechaI$s' AND 's$fechaF$s'
	AND COMPROBANTE_CNT.ANULADO != -1
	AND COMPROBANTE_CNT.TIPO = 'NCL'
	AND 
	COMPROBANTE_CNT.NUMERO NOT IN (
	SELECT
		NUMFORMATO
	FROM
		ESTADO_NOTAS)
ORDER BY
	COMPROBANTE_CNT.COMPANIA,
	COMPROBANTE_CNT.ANO,
	COMPROBANTE_CNT.TIPO,
	COMPROBANTE_CNT.NUMERO DESC,
	COMPROBANTE_CNT.FECHA DESC]') CONSULTA, 5 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);