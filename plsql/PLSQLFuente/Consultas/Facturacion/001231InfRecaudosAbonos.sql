SELECT 
   USOS.NOMBRE       NOMBREUSO
  ,ESTRATOS.NOMBRE   NOMBREESTRATO
  ,CONCEPTOS.SERVICIO
  ,D_ABONOS.CONCEPTO CODIGOCONCEPTO
  ,CONCEPTOS.NOMBRE  NOMBRECONCEPTO
  ,SUM(CASE WHEN D_ABONOS.CONCEPTO <> 16
		    THEN VALORACT
		    ELSE 0	
	   END) VALORCORRIENTE
  ,SUM(CASE WHEN D_ABONOS.CONCEPTO <> 16
		    THEN VALORANT
		    ELSE 0
	   END) VALORDEUDA
  ,SUM(CASE WHEN D_ABONOS.CONCEPTO = 16
		    THEN VALORACT
		    ELSE 0
	   END) RECARGOCORRIENTE
  ,SUM(CASE WHEN D_ABONOS.CONCEPTO = 16
	        THEN VALORANT
		    ELSE 0
	   END) RECARGODEUDA
  ,SUM(CASE WHEN D_ABONOS.CONCEPTO <> 16
		    THEN VALORACT
		    ELSE 0	
	   END + 	
       CASE WHEN D_ABONOS.CONCEPTO = 16
		    THEN VALORACT
		    ELSE 0
	   END) TOTALCORRIENTE
  ,SUM(CASE WHEN D_ABONOS.CONCEPTO <> 16
		    THEN VALORANT
		    ELSE 0
       END +
       CASE WHEN D_ABONOS.CONCEPTO = 16
		    THEN VALORANT
		    ELSE 0
	   END) TOTALVENCIDO
  ,SUM(CASE WHEN D_ABONOS.CONCEPTO <> 16
		    THEN VALORACT
		    ELSE 0	
	   END + 	
       CASE WHEN D_ABONOS.CONCEPTO = 16
		    THEN VALORACT
		    ELSE 0
	   END +
       CASE WHEN D_ABONOS.CONCEPTO <> 16
		    THEN VALORANT
		    ELSE 0
       END +
       CASE WHEN D_ABONOS.CONCEPTO = 16
		    THEN VALORANT
		    ELSE 0
	   END) TOTALRECAUDO  
FROM SP_USUARIO USUARIO
  INNER JOIN SP_ABONOS ABONOS 
     ON USUARIO.COMPANIA 	= ABONOS.COMPANIA 
    AND USUARIO.CICLO 		= ABONOS.CICLO
    AND USUARIO.CODIGORUTA 	= ABONOS.CODIGORUTA
  INNER JOIN SP_ESTRATOS ESTRATOS 
     ON USUARIO.COMPANIA 	= ESTRATOS.COMPANIA 
    AND USUARIO.USO 		  = ESTRATOS.USO
    AND USUARIO.ESTRATO 	= ESTRATOS.CODIGO
  INNER JOIN SP_USOS USOS 
     ON USUARIO.COMPANIA = USOS.COMPANIA
    AND USUARIO.USO 	   = USOS.CODIGO   
  INNER JOIN SP_D_ABONOS D_ABONOS 
     ON ABONOS.COMPANIA 	  = D_ABONOS.COMPANIA 
    AND ABONOS.CICLO 		    = D_ABONOS.CICLO
    AND ABONOS.CODIGORUTA 	= D_ABONOS.CODIGORUTA
  INNER JOIN SP_CONCEPTOS CONCEPTOS 
     ON D_ABONOS.COMPANIA = CONCEPTOS.COMPANIA
    AND D_ABONOS.CONCEPTO = CONCEPTOS.CODIGO   
WHERE USUARIO.COMPANIA = s$compania$s
  AND USUARIO.CICLO  BETWEEN s$cicloInicial$s AND s$cicloFinal$s
  AND ABONOS.PERIODO BETWEEN '01' AND '10' 
  AND ABONOS.FECHA   BETWEEN s$fechaInicial$s AND s$fechaFinal$s
GROUP BY 
   USOS.NOMBRE
  ,ESTRATOS.NOMBRE
  ,CONCEPTOS.SERVICIO
  ,D_ABONOS.CONCEPTO
  ,CONCEPTOS.NOMBRE
ORDER BY 
   ESTRATOS.NOMBRE
  ,CONCEPTOS.SERVICIO
  ,D_ABONOS.CONCEPTO
