SELECT COMPANIA,
       CICLO,
       CODIGORUTA, 
       CODIGOINTERNO,
       (NOMBRES||' '||PRIMERAPELLIDO||' '||SEGUNDOAPELLIDO) NOMBRECOMPLETO,
       DIRTECNICA,
       NIT, 
       ANO, 
       PERIODO,
       USO,
       ESTRATO,
       ESTRATOASEO,
       CARGOFIJOAL FIJO,
       SUBFIJOALC SUBSIDIOFIJO, 
       SOBREFIJOALC SOBFIJO,
       (CARGOFIJOAL+SUBFIJOALC-SOBREFIJOALC) SUBTOTALFIJO,
       VALORALCBASICO BASICO,
       VALORALCCOMPLEMENTARIO COMPLEMENTARIO,
       VALORALCSUNTUARIO SUNTUARIO, 
       (VALORALCBASICO+VALORALCCOMPLEMENTARIO+VALORALCSUNTUARIO) TOTCONSUMO,
       SUBCONSUMOAL SUBCONSUMO,
       SOBRECONSUMOAL SOBRECONSUMO
  FROM SP_USUARIO 
 WHERE SP_USUARIO.COMPANIA = s$compania$s
   AND SP_USUARIO.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
   AND SP_USUARIO.CICLO BETWEEN s$cicloInicial$s AND s$codigoFinal$s
   AND SP_USUARIO.ESTADO <> 'R' 
   AND SP_USUARIO.ALCANTARILLADO NOT IN (0)
