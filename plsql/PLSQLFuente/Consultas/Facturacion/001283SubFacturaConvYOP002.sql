SELECT   EMPRESAS_CONVENIO.NOMBRE        
       , HISTORIA_CONVENIOS.TOTAL        
       , HISTORIA_CONVENIOS.NOCOBRAR        
       , HISTORIA_CONVENIOS.COMPANIA        
       , HISTORIA_CONVENIOS.CICLO        
       , HISTORIA_CONVENIOS.CODIGORUTA        
       , HISTORIA_CONVENIOS.ANO        
       , HISTORIA_CONVENIOS.PERIODO        
       , HISTORIA_CONVENIOS.FECHA_PAGO 
FROM SP_EMPRESAS_CONVENIO EMPRESAS_CONVENIO      
INNER JOIN SP_HISTORIA_CONVENIOS HISTORIA_CONVENIOS         
 ON  EMPRESAS_CONVENIO.COMPANIA = HISTORIA_CONVENIOS.COMPANIA         
 AND EMPRESAS_CONVENIO.ID       = HISTORIA_CONVENIOS.ID_EMPRESA 
  WHERE HISTORIA_CONVENIOS.COMPANIA = $P{PR_COMPANIA}   
  AND HISTORIA_CONVENIOS.ANO = $P{PR_ANO}   
  AND HISTORIA_CONVENIOS.CICLO = $P{PR_CICLO}  
   AND HISTORIA_CONVENIOS.PERIODO = $P{PR_PERIODO}  
 AND HISTORIA_CONVENIOS.CODIGORUTA = $P{PR_CODIGORUTA}
