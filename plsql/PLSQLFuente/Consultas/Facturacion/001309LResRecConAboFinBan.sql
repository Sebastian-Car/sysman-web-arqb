WITH DOBLESAGRUPSINUSUARIO AS (
  SELECT   SP_D_PAGOSDOBLES.COMPANIA
          ,SP_D_PAGOSDOBLES.FECHA
          ,SP_D_PAGOSDOBLES.BANCO
          ,SP_D_PAGOSDOBLES.NUMEROPAQUETE
          ,SP_D_PAGOSDOBLES.CONCEPTO
          ,SUM(SP_D_PAGOSDOBLES.VALOR) TOTALES
  FROM     SP_D_PAGOSDOBLES
  GROUP BY SP_D_PAGOSDOBLES.COMPANIA
          ,SP_D_PAGOSDOBLES.FECHA
          ,SP_D_PAGOSDOBLES.BANCO
          ,SP_D_PAGOSDOBLES.NUMEROPAQUETE
          ,SP_D_PAGOSDOBLES.CONCEPTO
)
,CNSRECAUPAGOSPERIODOBANCOPER AS (
  SELECT   SP_D_RECAUDO.COMPANIA
          ,SUM(SP_D_RECAUDO.VALORPAGOPERIODO) SUMADEVALORPAGOPERIODO
          ,SP_D_RECAUDO.CONCEPTO
          ,SP_CONCEPTOS.NOMBRE NOMBRECONCEPTO
          ,SUM(SP_D_RECAUDO.VALORDEUDA) SUMADEVALORDEUDA
          ,SUM(SP_D_RECAUDO.VALORFINACT) SUMADEVALORFINACT
          ,SUM(SP_D_RECAUDO.VALORFINANT) SUMADEVALORFINANT
          ,SUM(NVL(DOBLESAGRUPSINUSUARIO.TOTALES,0)) DBLTOTALES
          ,SUM(NVL(SP_D_RECAUDO.CREDITOABONADO,0)) SUMADECREDITOABONADO
          ,SUM(SP_D_RECAUDO.VALORABONOACT) SUMADEVALORABONOACT
          ,SUM(SP_D_RECAUDO.VALORABONOANT) SUMADEVALORABONOANT
          ,SP_D_RECAUDO.BANCO
  FROM     SP_D_RECAUDO
    LEFT JOIN SP_CONCEPTOS
      ON      SP_D_RECAUDO.COMPANIA = SP_CONCEPTOS.COMPANIA
     AND      SP_D_RECAUDO.CONCEPTO = SP_CONCEPTOS.CODIGO
    LEFT JOIN DOBLESAGRUPSINUSUARIO
      ON      SP_D_RECAUDO.COMPANIA      = DOBLESAGRUPSINUSUARIO.COMPANIA
     AND      SP_D_RECAUDO.FECHA         = DOBLESAGRUPSINUSUARIO.FECHA
     AND      SP_D_RECAUDO.BANCO         = DOBLESAGRUPSINUSUARIO.BANCO
     AND      SP_D_RECAUDO.NUMEROPAQUETE = DOBLESAGRUPSINUSUARIO.NUMEROPAQUETE
     AND      SP_D_RECAUDO.CONCEPTO      = DOBLESAGRUPSINUSUARIO.CONCEPTO
  WHERE    SP_D_RECAUDO.COMPANIA   = s$compania$s
    AND    SP_D_RECAUDO.FECHA      IS NOT NULL
    AND    SP_D_RECAUDO.FECHA      BETWEEN s$fechaInicial$s AND s$fechaFinal$s
    AND    (SP_D_RECAUDO.CONCEPTO  BETWEEN 1 AND 48
     OR    SP_D_RECAUDO.CONCEPTO   IN (201,202,203,204,205,206,207,246,247,248,249))
    AND    (SP_D_RECAUDO.CONCEPTO) NOT IN (12,17)
  GROUP BY SP_D_RECAUDO.CONCEPTO
          ,SP_CONCEPTOS.NOMBRE
          ,SP_D_RECAUDO.COMPANIA
          ,SP_D_RECAUDO.BANCO
  HAVING   SUM(SP_D_RECAUDO.VALORPAGOPERIODO
             + SP_D_RECAUDO.VALORDEUDA
             + SP_D_RECAUDO.VALORFINANT
             + SP_D_RECAUDO.VALORFINACT
             + SP_D_RECAUDO.VALORABONOACT
             + SP_D_RECAUDO.VALORABONOANT
             + NVL(DOBLESAGRUPSINUSUARIO.TOTALES,0)
             - NVL(SP_D_RECAUDO.CREDITOABONADO,0)) <> 0
    UNION
    SELECT   DOBLESAGRUPSINUSUARIO.COMPANIA
            ,0 VALORPAGOPERIODO
            ,DOBLESAGRUPSINUSUARIO.CONCEPTO
            ,SP_CONCEPTOS.NOMBRE NOMBRECONCEPTO
            ,0 VALORDEUDA
            ,0 VALORFINACT
            ,0 VALORFINANT
            ,SUM(NVL(DOBLESAGRUPSINUSUARIO.TOTALES,0)) DBLTOTALES
            ,0 CREDITOABONADO
            ,0 VALORABONOACT
            ,0 VALORABONOANT
            ,DOBLESAGRUPSINUSUARIO.BANCO
    FROM DOBLESAGRUPSINUSUARIO
      LEFT JOIN SP_D_RECAUDO
        ON      DOBLESAGRUPSINUSUARIO.COMPANIA      = SP_D_RECAUDO.COMPANIA
       AND      DOBLESAGRUPSINUSUARIO.CONCEPTO      = SP_D_RECAUDO.CONCEPTO
       AND      DOBLESAGRUPSINUSUARIO.NUMEROPAQUETE = SP_D_RECAUDO.NUMEROPAQUETE
       AND      DOBLESAGRUPSINUSUARIO.BANCO         = SP_D_RECAUDO.BANCO
       AND      DOBLESAGRUPSINUSUARIO.FECHA         = SP_D_RECAUDO.FECHA
      LEFT JOIN SP_CONCEPTOS
        ON      DOBLESAGRUPSINUSUARIO.COMPANIA  = SP_CONCEPTOS.COMPANIA
       AND      DOBLESAGRUPSINUSUARIO.CONCEPTO  = SP_CONCEPTOS.CODIGO  
    WHERE    DOBLESAGRUPSINUSUARIO.COMPANIA  = s$compania$s
      AND    (DOBLESAGRUPSINUSUARIO.CONCEPTO BETWEEN 1 AND 48
       OR    DOBLESAGRUPSINUSUARIO.CONCEPTO  IN (201,202,203,204,205,206,207,246,247,248,249))
      AND    DOBLESAGRUPSINUSUARIO.CONCEPTO  NOT IN (12,17)
      AND    SP_D_RECAUDO.COMPANIA           IS NULL
      AND    DOBLESAGRUPSINUSUARIO.FECHA     IS NOT NULL
      AND    SP_D_RECAUDO.FECHA              BETWEEN s$fechaInicial$s AND s$fechaFinal$s
    GROUP BY DOBLESAGRUPSINUSUARIO.CONCEPTO
            ,SP_CONCEPTOS.NOMBRE
            ,DOBLESAGRUPSINUSUARIO.COMPANIA
            ,DOBLESAGRUPSINUSUARIO.BANCO
    HAVING   SUM(NVL(DOBLESAGRUPSINUSUARIO.TOTALES,0)) <> 0
)
SELECT   SUM(CNSRECAUPAGOSPERIODOBANCOPER.SUMADEVALORPAGOPERIODO) SUMADEVALORPAGOPERIODO
        ,CNSRECAUPAGOSPERIODOBANCOPER.CONCEPTO
        ,CNSRECAUPAGOSPERIODOBANCOPER.NOMBRECONCEPTO
        ,SUM(CNSRECAUPAGOSPERIODOBANCOPER.SUMADEVALORDEUDA) SUMADEVALORDEUDA
        ,SUM(CNSRECAUPAGOSPERIODOBANCOPER.SUMADEVALORFINACT) SUMADEVALORFINACT
        ,SUM(CNSRECAUPAGOSPERIODOBANCOPER.SUMADEVALORFINANT) SUMADEVALORFINANT
        ,SUM(CNSRECAUPAGOSPERIODOBANCOPER.DBLTOTALES) DBLTOTALES
        ,SUM(CNSRECAUPAGOSPERIODOBANCOPER.SUMADECREDITOABONADO) SUMADECREDITOABONADO
        ,SP_BANCOS.NOMBRE NOMBREBANCO
        ,SUM(CNSRECAUPAGOSPERIODOBANCOPER.SUMADEVALORABONOACT) SUMADEVALORABONOACT
        ,SUM(CNSRECAUPAGOSPERIODOBANCOPER.SUMADEVALORABONOANT) SUMADEVALORABONOANT
FROM     CNSRECAUPAGOSPERIODOBANCOPER
  INNER JOIN SP_BANCOS
     ON      CNSRECAUPAGOSPERIODOBANCOPER.COMPANIA = SP_BANCOS.COMPANIA
    AND      CNSRECAUPAGOSPERIODOBANCOPER.BANCO    = SP_BANCOS.CODIGO 
s$condicion$s
GROUP BY CNSRECAUPAGOSPERIODOBANCOPER.CONCEPTO
        ,CNSRECAUPAGOSPERIODOBANCOPER.NOMBRECONCEPTO
        ,SP_BANCOS.NOMBRE
