WITH XLSACUELECTURASXRANGO AS (
  SELECT * FROM(
    SELECT SP_ESTADISTICAS_CONSUMO.COMPANIA,
      SP_TARIFAS.USOSUPERSERVICIOS,
      CASE WHEN USOSUPERSERVICIOS='1' THEN SP_ESTADISTICAS_CONSUMO.ESTRATO ELSE '1' END  AS ESTRATO,
      SP_ESTADISTICAS_CONSUMO.RANGO,
      SP_ESTADISTICAS_CONSUMO.CONSUMO,
      SP_ESTADISTICAS_CONSUMO.CONSUMOPROMEDIO,
      LPAD(SP_ESTADISTICAS_CONSUMO.ANO,4) || LPAD(SP_ESTADISTICAS_CONSUMO.PERIODO,2) AS PERIODO
    FROM SP_ESTADISTICAS_CONSUMO
      LEFT JOIN SP_TARIFAS
        ON  SP_ESTADISTICAS_CONSUMO.COMPANIA = SP_TARIFAS.COMPANIA
        AND SP_ESTADISTICAS_CONSUMO.ANO = SP_TARIFAS.ANO
        AND SP_ESTADISTICAS_CONSUMO.PERIODO  = SP_TARIFAS.PERIODO
        AND SP_ESTADISTICAS_CONSUMO.USO = SP_TARIFAS.USO
        AND SP_ESTADISTICAS_CONSUMO.ESTRATO = SP_TARIFAS.ESTRATO
    WHERE SP_ESTADISTICAS_CONSUMO.COMPANIA =s$compania$s
      AND LPAD(SP_ESTADISTICAS_CONSUMO.ANO,4) || LPAD(SP_ESTADISTICAS_CONSUMO.PERIODO,2) 
      BETWEEN LPAD('s$anoInicial$s',4) || LPAD('s$periodoInicial$s',2) AND LPAD('s$anoFinal$s',4)  || LPAD('s$periodoFinal$s',2)
      AND SP_ESTADISTICAS_CONSUMO.SERVICIO ='01'
      AND SP_TARIFAS.USOSUPERSERVICIOS IS NOT NULL
      AND SP_TARIFAS.USOSUPERSERVICIOS IS NOT NULL
    GROUP BY SP_ESTADISTICAS_CONSUMO.COMPANIA,
      SP_TARIFAS.USOSUPERSERVICIOS,
      CASE WHEN USOSUPERSERVICIOS='1' THEN SP_ESTADISTICAS_CONSUMO.ESTRATO ELSE '1' END,
      SP_ESTADISTICAS_CONSUMO.RANGO,
      SP_ESTADISTICAS_CONSUMO.CONSUMO,
      SP_ESTADISTICAS_CONSUMO.CONSUMOPROMEDIO,
      LPAD(SP_ESTADISTICAS_CONSUMO.ANO,4) || LPAD(SP_ESTADISTICAS_CONSUMO.PERIODO,2))
  PIVOT (SUM(NVL(CONSUMO,0)+NVL(CONSUMOPROMEDIO,0)) FOR PERIODO IN(s$inPivot$s))
  ORDER BY USOSUPERSERVICIOS,ESTRATO,RANGO
),    
XLSACUENOUSUARIOSESTRATO AS (
  SELECT * FROM (
    SELECT SP_ESTADISTICAS_CONSUMO.COMPANIA,
      SP_TARIFAS.USOSUPERSERVICIOS,
      CASE WHEN USOSUPERSERVICIOS='1'  THEN SP_ESTADISTICAS_CONSUMO.ESTRATO  ELSE '1' END  AS ESTRATO, 
      '00' AS RANGO,
      SP_ESTADISTICAS_CONSUMO.CANTUSUARIOSACUEDUCTO,
      LPAD(SP_ESTADISTICAS_CONSUMO.ANO,4) || LPAD(SP_ESTADISTICAS_CONSUMO.PERIODO,2) AS PERIODO
    FROM SP_ESTADISTICAS_CONSUMO
      LEFT JOIN SP_TARIFAS
        ON  SP_ESTADISTICAS_CONSUMO.COMPANIA = SP_TARIFAS.COMPANIA
        AND SP_ESTADISTICAS_CONSUMO.ANO = SP_TARIFAS.ANO
        AND SP_ESTADISTICAS_CONSUMO.PERIODO = SP_TARIFAS.PERIODO
        AND SP_ESTADISTICAS_CONSUMO.USO = SP_TARIFAS.USO
        AND SP_ESTADISTICAS_CONSUMO.ESTRATO = SP_TARIFAS.ESTRATO
    WHERE SP_ESTADISTICAS_CONSUMO.COMPANIA =s$compania$s
      AND SP_ESTADISTICAS_CONSUMO.SERVICIO  ='01'
      AND SP_TARIFAS.USOSUPERSERVICIOS IS NOT NULL
      AND LPAD(SP_ESTADISTICAS_CONSUMO.ANO,4) || LPAD(SP_ESTADISTICAS_CONSUMO.PERIODO,2) 
      BETWEEN LPAD('s$anoInicial$s',4) || LPAD('s$periodoInicial$s',2) AND LPAD('s$anoFinal$s',4)  || LPAD('s$periodoFinal$s',2)
    GROUP BY SP_ESTADISTICAS_CONSUMO.COMPANIA,
      SP_TARIFAS.USOSUPERSERVICIOS,
      CASE WHEN USOSUPERSERVICIOS ='1' THEN SP_ESTADISTICAS_CONSUMO.ESTRATO ELSE '1'END,
      SP_ESTADISTICAS_CONSUMO.CANTUSUARIOSACUEDUCTO,
      LPAD(SP_ESTADISTICAS_CONSUMO.ANO,4) || LPAD(SP_ESTADISTICAS_CONSUMO.PERIODO,2)
    ORDER BY SP_ESTADISTICAS_CONSUMO.COMPANIA,
      SP_ESTADISTICAS_CONSUMO.CANTUSUARIOSACUEDUCTO,
      LPAD(SP_ESTADISTICAS_CONSUMO.ANO,4) || LPAD(SP_ESTADISTICAS_CONSUMO.PERIODO,2))
  PIVOT (SUM(CANTUSUARIOSACUEDUCTO) FOR PERIODO IN(s$inPivot$s))
  ORDER BY USOSUPERSERVICIOS,ESTRATO
)
SELECT * FROM XLSACUELECTURASXRANGO UNION SELECT * FROM XLSACUENOUSUARIOSESTRATO
