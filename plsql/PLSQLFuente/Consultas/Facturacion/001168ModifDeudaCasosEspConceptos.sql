SELECT SP_MODIFICACIONESDEUDA.COMPANIA,
       (PRIMERAPELLIDO||' '||SEGUNDOAPELLIDO||' '||NOMBRES) NOMBRECOMPLETO,
       SP_CONCEPTOS.NOMBRE NOMBRECONCEPTO,
       (VRANT-VRNUE) DIFERENCIA,
       SP_MODIFICACIONESDEUDA.VRANT,
       SP_MODIFICACIONESDEUDA.VRNUE,
       SP_MODIFICACIONESDEUDA.TIPOMODIFICACION,
       SP_MODIFICACIONESDEUDA.ANO||SP_MODIFICACIONESDEUDA.PERIODO PERIODOUNICO,
       SP_MODIFICACIONESDEUDA.CODIGORUTA,
       SP_MODIFICACIONESDEUDA.CICLO,
       SP_MODIFICACIONESDEUDA.CONCEPTO,
       SP_MODIFICACIONESDEUDA.USUARIO,
       SP_MODIFICACIONESDEUDA.CAUSAMODIFICACION,
       SP_MODIFICACIONESDEUDA.ANO,
       SP_MODIFICACIONESDEUDA.PERIODO,
       SP_MODIFICACIONESDEUDA.FECHA,
       SP_MODIFICACIONESDEUDA.HORA 
  FROM SP_MODIFICACIONESDEUDA
    LEFT JOIN SP_CONCEPTOS
       ON SP_MODIFICACIONESDEUDA.COMPANIA = SP_CONCEPTOS.COMPANIA
      AND SP_MODIFICACIONESDEUDA.CONCEPTO  = SP_CONCEPTOS.CODIGO
    LEFT JOIN SP_USUARIO
       ON SP_MODIFICACIONESDEUDA.COMPANIA     = SP_USUARIO.COMPANIA
      AND SP_MODIFICACIONESDEUDA.CICLO        = SP_USUARIO.CICLO
      AND SP_MODIFICACIONESDEUDA.CODIGORUTA    = SP_USUARIO.CODIGORUTA
 WHERE SP_MODIFICACIONESDEUDA.COMPANIA = s$compania$s
   AND VRANT-VRNUE BETWEEN s$valorInicial$s AND s$valorFinal$s
   AND BANCOPERANTERIOR IS NULL
   AND SP_MODIFICACIONESDEUDA.TIPOMODIFICACION = 1
   AND FECHA BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
   AND SP_MODIFICACIONESDEUDA.ANO||SP_MODIFICACIONESDEUDA.PERIODO BETWEEN s$periodoInicial$s AND s$periodoFinal$s
   AND SP_MODIFICACIONESDEUDA.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
   AND (SP_MODIFICACIONESDEUDA.CONCEPTO BETWEEN 1 AND 49 OR SP_MODIFICACIONESDEUDA.CONCEPTO IN (247,248,249))
   s$ciclo$s
ORDER BY SP_USUARIO.CICLO
