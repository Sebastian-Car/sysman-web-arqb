SELECT SP_USUARIO.COMPANIA, 
    SP_USUARIO.CODIGORUTA, 
    SP_USUARIO.NOMBRES, 
    NVL(SP_USUARIO.PRIMERAPELLIDO, ' ') PRIMERAPELLIDO, 
    NVL(SP_USUARIO.SEGUNDOAPELLIDO, ' ') SEGUNDOAPELLIDO, 
    SP_USUARIO.DIRTECNICA, 
    SP_USUARIO.USO, 
    SP_USUARIO.ESTRATO, 
    SP_USUARIO.FECHARENTRADANUEVOUSUARIO, 
    DECODE(SP_USUARIO.ACUEDUCTO, 0, 'No', 'Sí') ACUEDUCTO, 
    DECODE(SP_USUARIO.ASEO, 0, 'No', 'Sí') ASEO, 
    DECODE(SP_USUARIO.ALCANTARILLADO, 0, 'No', 'Sí') ALCANTARILLADO, 
    SP_USUARIO.LECTURA, 
    SP_USUARIO.LECTURA1, 
    SP_USOS.NOMBRE AS NOMBREU
,
    s$agrupado$s AGRUPADO
FROM SP_USUARIO INNER JOIN SP_USOS ON 
    SP_USUARIO.COMPANIA = SP_USOS.COMPANIA
    AND SP_USUARIO.USO = SP_USOS.CODIGO 
WHERE SP_USUARIO.COMPANIA = s$compania$s
    s$condicionCiclo$s
    AND SP_USUARIO.FECHARENTRADANUEVOUSUARIO BETWEEN s$fechaInicial$s AND s$fechaFinal$s
ORDER BY SP_USUARIO.COMPANIA, 
    SP_USUARIO.CICLO, 
    SP_USUARIO.CODIGORUTA
