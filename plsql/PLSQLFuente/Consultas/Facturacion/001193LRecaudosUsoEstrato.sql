SELECT  
    USUARIO.COMPANIA,  
    USUARIO.CICLO,  
    USUARIO.CODIGORUTA,  
    USUARIO.BANCOPERPROCESO,  
    USUARIO.PAQUETEPAGOPERPROCESO,  
    USUARIO.FECHAPAGOPERPROCESO,  
    FACTURADO.VALOR_FACTURADO,  
    FACTURADO.DEUDA,  
    FACTURADO.DOBLEPAGO,  
    FACTURADO.VALOR_FACTURADO + FACTURADO.DEUDA + FACTURADO.DOBLEPAGO TOTALRECAUDADO,  
    USUARIO.BANCOPERPROCESO || PAQUETEPAGOPERPROCESO BANCOYPAQUETE,  
    USUARIO.USO,  
    USUARIO.ESTRATO,  
    USUARIO.PERIODOSATRASO,  
    USOS.NOMBRE NOMBREUSO,  
    ESTRATOS.NOMBRE NOMBREESTRATO  
FROM  
    SP_USUARIO USUARIO  
        LEFT JOIN SP_ESTRATOS ESTRATOS  
        ON USUARIO.COMPANIA = ESTRATOS.COMPANIA  
            AND  
        USUARIO.USO = ESTRATOS.USO  
            AND  
        USUARIO.ESTRATO = ESTRATOS.CODIGO  
            LEFT JOIN SP_USOS USOS  
            ON ESTRATOS.COMPANIA = USOS.COMPANIA  
                AND  
            ESTRATOS.USO = USOS.CODIGO  
                LEFT JOIN SP_FACTURADO FACTURADO  
                ON USUARIO.COMPANIA = FACTURADO.COMPANIA  
                    AND  
                USUARIO.CICLO = FACTURADO.CICLO  
                    AND  
                USUARIO.CODIGORUTA = FACTURADO.CODIGORUTA  
WHERE USUARIO.COMPANIA = s$compania$s  
   s$ciclo$s 
  AND FACTURADO.VALOR_FACTURADO + FACTURADO.DEUDA + FACTURADO.DOBLEPAGO NOT IN(0)  
  AND FECHAPAGOPERPROCESO BETWEEN TO_DATE('s$fechaInicial$s', 'DD/MM/YYYY')  
                              And TO_DATE('s$fechaFinal$s', 'DD/MM/YYYY')  
  AND (FACTURADO.CONCEPTO BETWEEN 1 AND 49  
       OR FACTURADO.CONCEPTO IN (201,202,203,204,205,206,207,246,247,248,249))
