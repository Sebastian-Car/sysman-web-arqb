SELECT SP_MODIFICACIONES.FECHA,
       SP_MODIFICACIONES.CODIGORUTA,
       SP_USUARIO.CODIGOINTERNO,
       SUM(CASE WHEN SP_CONCEPTOS.SERVICIO='01' THEN VRANT ELSE 0 END) valorAntAcu,
       SUM(CASE WHEN SP_CONCEPTOS.SERVICIO='01' THEN VRNUE ELSE 0 END) valorNueAcu,
       SUM(CASE WHEN SP_CONCEPTOS.SERVICIO='02' THEN VRANT ELSE 0 END) valorAntAlc,
       SUM(CASE WHEN SP_CONCEPTOS.SERVICIO='02' THEN VRNUE ELSE 0 END) valorNueAlc,
       SUM(CASE WHEN SP_CONCEPTOS.SERVICIO='03' THEN VRANT ELSE 0 END) valorAntAse,
       SUM(CASE WHEN SP_CONCEPTOS.SERVICIO='03' THEN VRNUE ELSE 0 END) valorNueAse,
       SP_MODIFICACIONES.TIPO,
       SP_MODIFICACIONES.OBSERVACIONES 
  FROM SP_USUARIO 
    INNER JOIN SP_MODIFICACIONES
         ON SP_USUARIO.COMPANIA = SP_MODIFICACIONES.COMPANIA
        AND SP_USUARIO.CODIGORUTA = SP_MODIFICACIONES.CODIGORUTA 
    INNER JOIN SP_MODIFICACIONESDEUDA 
       ON SP_MODIFICACIONES.COMPANIA = SP_MODIFICACIONESDEUDA.COMPANIA
      AND SP_MODIFICACIONES.FECHA = SP_MODIFICACIONESDEUDA.FECHA
      AND SP_MODIFICACIONES.HORA = SP_MODIFICACIONESDEUDA.HORA
      AND SP_MODIFICACIONES.CODIGORUTA = SP_MODIFICACIONESDEUDA.CODIGORUTA
      AND SP_MODIFICACIONES.CICLO = SP_MODIFICACIONESDEUDA.CICLO
    INNER JOIN SP_CONCEPTOS
       ON SP_MODIFICACIONESDEUDA.COMPANIA = SP_CONCEPTOS.COMPANIA
      AND SP_MODIFICACIONESDEUDA.CONCEPTO = SP_CONCEPTOS.CODIGO
 WHERE SP_USUARIO.COMPANIA = s$compania$s
   AND SP_MODIFICACIONES.FECHA BETWEEN TO_DATE('s$fechaInicial$s', 'DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s', 'DD/MM/YYYY')
   s$ciclo$s
   AND SP_MODIFICACIONES.TIPO=s$tipo$s
GROUP BY SP_MODIFICACIONES.FECHA, SP_MODIFICACIONES.CODIGORUTA, SP_USUARIO.CODIGOINTERNO, SP_MODIFICACIONES.TIPO, SP_MODIFICACIONES.OBSERVACIONES
ORDER BY SP_MODIFICACIONES.FECHA DESC
