SELECT  SP_USUARIO.CODIGORUTA, 
        SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO || ' ' ||  SP_USUARIO.NOMBRES AS NOMBRECOMPLETO, 
        SP_CONCEPTOS.CODIGO, 
        SP_CONCEPTOS.NOMBRE AS NOMBRECONCEPTO, 
        SP_FACTURADO.VALOR_FACTURADOANT, 
        SP_FACTURADO.DEUDAANT, 
        (SP_FACTURADO.VALOR_FACTURADOANT+SP_FACTURADO.DEUDAANT) AS TOTALANTERIOR, 
        SP_FACTURADO.DEUDA, 
       (SP_FACTURADO.VALOR_FACTURADOANT+SP_FACTURADO.DEUDAANT)-SP_FACTURADO.DEUDA AS DIFERENCIA, 
        SP_USUARIO.COMPANIA, 
        (LPAD(SP_FACTURADO.ANO,4,'0') || LPAD(SP_FACTURADO.PERIODO,2,'0')) AS PERIODOUNICO 
FROM SP_USUARIO 
  INNER JOIN SP_FACTURADO 
    ON  SP_USUARIO.COMPANIA = SP_FACTURADO.COMPANIA 
    AND SP_USUARIO.CICLO = SP_FACTURADO.CICLO 
    AND  SP_USUARIO.CODIGORUTA = SP_FACTURADO.CODIGORUTA 
  LEFT JOIN SP_CONCEPTOS 
    ON  SP_FACTURADO.CONCEPTO = SP_CONCEPTOS.CODIGO 
    AND SP_FACTURADO.COMPANIA = SP_CONCEPTOS.COMPANIA
WHERE SP_USUARIO.COMPANIA = s$compania$s
  AND SP_FACTURADO.CICLO =  s$ciclo$s  
  AND SP_USUARIO.CODIGORUTA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s' 
  AND ((SP_FACTURADO.VALOR_FACTURADOANT + SP_FACTURADO.DEUDAANT) <> SP_FACTURADO.DEUDA) 
  AND (LPAD(SP_FACTURADO.ANO,4,'0') || LPAD(SP_FACTURADO.PERIODO,2,'0'))  BETWEEN (LPAD('s$anoInicial$s',4,'0') || LPAD('s$periodoInicial$s',2,'0')) AND (LPAD('s$anoFinal$s',4,'0') || LPAD('s$periodoFinal$s',2,'0')) 
  AND SP_FACTURADO.CONCEPTO NOT IN (16,23,27,30,31,250) 
  AND BANCOPERANTERIOR IS NULL
