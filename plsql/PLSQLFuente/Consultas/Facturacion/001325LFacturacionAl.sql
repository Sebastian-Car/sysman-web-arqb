SELECT DISTINCT
 USUARIO.COMPANIA, 
 USUARIO.CICLO, 
 USUARIO.CODIGORUTA, 
 USUARIO.ANO, 
 USUARIO.PERIODO,  
 USUARIO.CODIGOINTERNO, 
 USUARIO.USO, 
 USUARIO.ESTRATO, 
 USUARIO.SUBALCANTARILLADO, 
 USUARIO.SOBREALCANTARILLADO, 
 USUARIO.TOTALALCANTARILLADO, 
 USUARIO.TOTFACTURAPERACTUAL, 
 DECODE(USUARIO.ALCANTARILLADO,-1,'SI','NO') ALCANTARILLADO,
 NVL(RECARGOACUEDUCTO,0)+NVL(RECARGOASEO,0) TOTALRECARGO, 
 TOTFACTURAPERACTUAL-TOTALDEUDA-TOTALASEO-TOTALALCANTARILLADO-TOTALACUEDUCTO OTROSCARGOS, 
 TOTALALCANTARILLADO-(CARGOFIJOAL+SUBFIJOALC-SOBREFIJOALC) TOTCONSUMO, 
 CARGOFIJOAL+SUBFIJOALC-SOBREFIJOALC TOTFIJO, 
 USUARIO.VALCONSUMO VC, 
 USUARIO.VALCONSUMOPROM VCP,
 BARRIOS.NOMBRE BARRIO,
 NVL(USUARIO.PRIMERAPELLIDO,' ') ||' '|| NVL(USUARIO.SEGUNDOAPELLIDO,' ')||' '|| NVL(USUARIO.NOMBRES,' ') NOMBRECOMPLETO
 FROM    SP_USUARIO USUARIO 
INNER JOIN BARRIOS 
ON USUARIO.BARRIO=BARRIOS.CODIGO
WHERE   USUARIO.COMPANIA = s$compania$s 
 s$condicion$s
 AND USUARIO.CODIGORUTA BETWEEN s$codigoInicial$s AND s$codigoFinal$s
 AND USUARIO.USO||USUARIO.ESTRATO BETWEEN s$usoIni$s||s$estratoIni$s AND  s$usoFin$s||s$estratoFin$s
 AND USUARIO.BARRIO BETWEEN s$barrioIni$s AND s$barrioFin$s
 s$condicionOrden$s
