SELECT  SP_USUARIO.CODIGORUTA, 
        SP_USUARIO.CODIGOINTERNO, 
        SP_USUARIO.NOMBRES || ' ' || SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO  SUSCRIPTOR,
        SP_USUARIO.DIRTECNICA,
        BARRIOS.NOMBRE BARRIO, 
        (CASE SP_USUARIO.AFOROINCENTIVO WHEN 0 THEN 'NO' ELSE 'SI' END) APLICA
FROM SP_USUARIO 
  INNER JOIN BARRIOS 
    ON SP_USUARIO.PAIS          = BARRIOS.PAIS
    AND SP_USUARIO.DEPARTAMENTO = BARRIOS.DEPARTAMENTO
    AND SP_USUARIO.CIUDAD       = BARRIOS.CIUDAD
    AND SP_USUARIO.BARRIO       = BARRIOS.CODIGO
WHERE SP_USUARIO.COMPANIA = s$compania$s
  AND SP_USUARIO.CICLO = s$cicloReporte$s
