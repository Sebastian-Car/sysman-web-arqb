WITH CUENTAMEDIDOR AS
  (SELECT SP_USUARIO.COMPANIA,
    SP_USUARIO.CICLO,
    SP_USUARIO.MEDIDOR,
    COUNT(SP_USUARIO.MEDIDOR)
  FROM SP_USUARIO
  WHERE SP_USUARIO.COMPANIA = 's$compania$s'
  AND SP_USUARIO.CICLO      = 's$ciclo$s'
  GROUP BY SP_USUARIO.COMPANIA,
    SP_USUARIO.CICLO,
    SP_USUARIO.MEDIDOR
  HAVING COUNT(SP_USUARIO.MEDIDOR)>1
  ORDER BY COUNT(SP_USUARIO.MEDIDOR)
  )
SELECT CUENTAMEDIDOR.COMPANIA,
  SP_USUARIO.CICLO,
  SP_USUARIO.CODIGORUTA,
  SP_USUARIO.CODIGOINTERNO,
  CUENTAMEDIDOR.MEDIDOR,
  SP_USUARIO.PRIMERAPELLIDO
  || ' '
  || SP_USUARIO.SEGUNDOAPELLIDO
  || ' '
  || SP_USUARIO.NOMBRES AS NOMBRES,
  SP_USUARIO.DIRTECNICA
FROM CUENTAMEDIDOR
LEFT JOIN SP_USUARIO
ON CUENTAMEDIDOR.MEDIDOR   = SP_USUARIO.MEDIDOR
AND CUENTAMEDIDOR.CICLO    = SP_USUARIO.CICLO
AND CUENTAMEDIDOR.COMPANIA = SP_USUARIO.COMPANIA
ORDER BY CUENTAMEDIDOR.MEDIDOR ,
  SP_USUARIO.PRIMERAPELLIDO
  || ' '
  || SP_USUARIO.SEGUNDOAPELLIDO
  || ' '
  || SP_USUARIO.NOMBRES
