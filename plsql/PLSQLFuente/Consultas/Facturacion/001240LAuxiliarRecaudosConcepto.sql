SELECT  SP_USUARIO.COMPANIA,
        SP_USUARIO.CODIGORUTA,
        SP_USUARIO.PRIMERAPELLIDO ||  ' ' || SP_USUARIO.SEGUNDOAPELLIDO || ' ' || SP_USUARIO.NOMBRES NOMBREUSUARIO, 
        SP_FACTURADO.DEUDA DEUDADEFINITIVA,
        SP_FACTURADO.VALOR_FACTURADO VALOR_FACTURADOFINAL,
        SP_FACTURADO.DEUDA +  SP_FACTURADO.VALOR_FACTURADO TOTALFACTURADO,
        SP_USUARIO.PERIODOSATRASO,
        SP_CONCEPTOS.CODIGO,
        SP_CONCEPTOS.NOMBRE NOMBRECONCEPTO,
        SP_FACTURADO.CONCEPTO
FROM  SP_USUARIO
  INNER JOIN  SP_FACTURADO
    ON SP_USUARIO.COMPANIA       = SP_FACTURADO.COMPANIA 
    AND SP_USUARIO.CICLO         = SP_FACTURADO.CICLO
    AND SP_USUARIO.CODIGORUTA    = SP_FACTURADO.CODIGORUTA
  INNER JOIN  SP_CONCEPTOS
    ON SP_FACTURADO.COMPANIA     = SP_CONCEPTOS.COMPANIA
    AND SP_FACTURADO.CONCEPTO    = SP_CONCEPTOS.CODIGO
WHERE SP_USUARIO.COMPANIA= s$compania$s
	  AND SP_USUARIO.CICLO BETWEEN s$cicloInicial$s AND s$cicloFinal$s
	  AND (SP_FACTURADO.CONCEPTO BETWEEN s$conceptoInicial$s AND s$conceptoFinal$s
          AND (SP_FACTURADO.CONCEPTO BETWEEN 0 AND 49 OR SP_FACTURADO.CONCEPTO IN (246,247,248,249)) 
          AND SP_FACTURADO.CONCEPTO NOT IN (12,17,49)) 
      AND SP_USUARIO.PERIODOSATRASO >= s$atraso$s 
      AND SP_USUARIO.ESTADO NOT IN('R','S')    
      AND SP_USUARIO.BANCOPERPROCESO IS NULL
      AND (((SP_FACTURADO.VALOR_FACTURADO + SP_FACTURADO.VALORFINACT - SP_FACTURADO.CREDITOABONADO- SP_FACTURADO.VALORABONOANT- SP_FACTURADO.VALORABONOACT) <> 0) 
      OR  ((SP_FACTURADO.DEUDA + SP_FACTURADO.VALORFINANT - SP_FACTURADO.VALORABONOANT)<>0) 
      OR  ((SP_FACTURADO.VALOR_FACTURADO + SP_FACTURADO.DEUDA + SP_FACTURADO.VALORFINACT +SP_FACTURADO.VALORFINANT-SP_FACTURADO.CREDITOABONADO-SP_FACTURADO.VALORABONOANT-SP_FACTURADO.VALORABONOACT)<>0))
