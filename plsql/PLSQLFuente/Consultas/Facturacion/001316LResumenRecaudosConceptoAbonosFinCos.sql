WITH DOBLESAGRUPSINUSUARIO AS(
  SELECT SP_D_PAGOSDOBLES.COMPANIA,
         SP_D_PAGOSDOBLES.FECHA,
         SP_D_PAGOSDOBLES.BANCO,
         SP_D_PAGOSDOBLES.NUMEROPAQUETE,
         SP_D_PAGOSDOBLES.CONCEPTO,
         SUM(SP_D_PAGOSDOBLES.VALOR) TOTALES
  FROM SP_D_PAGOSDOBLES
  GROUP BY SP_D_PAGOSDOBLES.COMPANIA,
           SP_D_PAGOSDOBLES.FECHA,
           SP_D_PAGOSDOBLES.BANCO,
           SP_D_PAGOSDOBLES.NUMEROPAQUETE,
           SP_D_PAGOSDOBLES.CONCEPTO),
CNSRECAUDOPAGOSPERIODOR AS(
  SELECT SP_D_RECAUDO.COMPANIA,
         SP_D_RECAUDO.CONCEPTO,
         SUM(SP_D_RECAUDO.VALORPAGOPERIODO) SUMADEVALORPAGOPERIODO,
         SUM(SP_D_RECAUDO.VALORDEUDA) SUMADEVALORDEUDA,
         SUM(NVL(TOTALES,0)) DBLTOTALES,
         SUM(SP_D_RECAUDO.VALORFINACT) SUMADEVALORFINACT,
         SUM(SP_D_RECAUDO.VALORFINANT) SUMADEVALORFINANT,
         SUM(SP_D_RECAUDO.VALORABONOACT) SUMADEVALORABONOACT,
         SUM(SP_D_RECAUDO.VALORABONOANT) SUMADEVALORABONOANT,
         SUM(SP_D_RECAUDO.CREDITOABONADO) SUMADECREDITOABONADO
  FROM SP_D_RECAUDO
    LEFT JOIN DOBLESAGRUPSINUSUARIO
      ON SP_D_RECAUDO.CONCEPTO       = DOBLESAGRUPSINUSUARIO.CONCEPTO
     AND SP_D_RECAUDO.NUMEROPAQUETE = DOBLESAGRUPSINUSUARIO.NUMEROPAQUETE
     AND SP_D_RECAUDO.BANCO         = DOBLESAGRUPSINUSUARIO.BANCO
     AND SP_D_RECAUDO.FECHA         = DOBLESAGRUPSINUSUARIO.FECHA
     AND SP_D_RECAUDO.COMPANIA      = DOBLESAGRUPSINUSUARIO.COMPANIA
  WHERE SP_D_RECAUDO.COMPANIA    = s$compania$s
    AND SP_D_RECAUDO.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
    AND (SP_D_RECAUDO.CONCEPTO BETWEEN 1 AND 48
     OR SP_D_RECAUDO.CONCEPTO      IN (246,247,248,249))
    AND SP_D_RECAUDO.CONCEPTO NOT IN (12,17)
  GROUP BY SP_D_RECAUDO.COMPANIA,
           SP_D_RECAUDO.CONCEPTO
  HAVING SUM(VALORPAGOPERIODO +VALORDEUDA+VALORFINANT+VALORFINACT-VALORABONOANT-VALORABONOACT-CREDITOABONADO)<>0)
SELECT SP_CONCEPTOS.CODIGO
      ,SP_CONCEPTOS.NOMBRE NOMBRECONCEPTO
      ,NVL(CNSRECAUDOPAGOSPERIODOR.SUMADEVALORPAGOPERIODO,0) SUMADEVALORPAGOPERIODO
      ,NVL(CNSRECAUDOPAGOSPERIODOR.SUMADEVALORDEUDA,0) SUMADEVALORDEUDA
      ,NVL( CNSRECAUDOPAGOSPERIODOR.DBLTOTALES,0) DBLTOTALES
      ,NVL((NVL(SUMADEVALORABONOANT,0)
          + NVL(SUMADEVALORABONOACT,0)
          + NVL(SUMADEVALORPAGOPERIODO,0)
          + NVL(SUMADEVALORDEUDA,0)
          + NVL(SUMADEVALORFINANT,0)
          + NVL(SUMADEVALORFINACT,0)
          - NVL(SUMADECREDITOABONADO,0)),0) TOTALREC
      ,NVL(CNSRECAUDOPAGOSPERIODOR.SUMADEVALORFINACT,0) SUMADEVALORFINACT
      ,NVL( CNSRECAUDOPAGOSPERIODOR.SUMADEVALORFINANT,0) SUMADEVALORFINANT
      ,NVL( CNSRECAUDOPAGOSPERIODOR.SUMADEVALORABONOACT,0) SUMADEVALORABONOACT
      ,NVL( CNSRECAUDOPAGOSPERIODOR.SUMADEVALORABONOANT,0) SUMADEVALORABONOANT
      ,NVL( CNSRECAUDOPAGOSPERIODOR.SUMADECREDITOABONADO,0) SUMADECREDITOABONADO
FROM   SP_CONCEPTOS
  LEFT JOIN CNSRECAUDOPAGOSPERIODOR
    ON      SP_CONCEPTOS.COMPANIA = CNSRECAUDOPAGOSPERIODOR.COMPANIA
   AND      SP_CONCEPTOS.CODIGO   = CNSRECAUDOPAGOSPERIODOR.CONCEPTO
WHERE  SP_CONCEPTOS.COMPANIA = s$compania$s
  AND  (SP_CONCEPTOS.CODIGO  BETWEEN 1 AND 48
   OR  SP_CONCEPTOS.CODIGO   IN (246,247,248,249))
  AND  SP_CONCEPTOS.CODIGO   NOT IN (12,17)
  AND  (SUMADEVALORABONOACT
      + SUMADEVALORABONOANT
      + SUMADEVALORFINANT
      + SUMADEVALORFINACT) <> 0
