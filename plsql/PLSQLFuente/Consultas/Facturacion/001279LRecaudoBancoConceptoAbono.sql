WITH CNSRECAUDOPAGOSPERIODOBANCO AS
  (SELECT SP_CONCEPTOS.COMPANIA,
    SP_CONCEPTOS.CODIGO,
    SP_D_RECAUDO.BANCO,
    SUM(SP_D_RECAUDO.VALORPAGOPERIODO) SUMADEVALORPAGOPERIODO,
    SUM(SP_D_RECAUDO.VALORDEUDA) SUMADEVALORDEUDA,
    SUM(VALORPAGOPERIODO+VALORDEUDA) TOTALRECAUDO,
    SUM(NVL(TOTALES,0)) DBLTOTALES
  FROM SP_CONCEPTOS
  INNER JOIN (SP_D_RECAUDO
  LEFT JOIN
    (SELECT SP_D_PAGOSDOBLES.COMPANIA,
      SP_D_PAGOSDOBLES.FECHA,
      SP_D_PAGOSDOBLES.BANCO,
      SP_D_PAGOSDOBLES.NUMEROPAQUETE,
      SP_D_PAGOSDOBLES.CONCEPTO,
      SUM(SP_D_PAGOSDOBLES.VALOR) TOTALES
    FROM SP_D_PAGOSDOBLES
    GROUP BY SP_D_PAGOSDOBLES.COMPANIA,
      SP_D_PAGOSDOBLES.FECHA,
      SP_D_PAGOSDOBLES.BANCO,
      SP_D_PAGOSDOBLES.NUMEROPAQUETE,
      SP_D_PAGOSDOBLES.CONCEPTO
    )DOBLESAGRUPSINUSUARIO
  ON (SP_D_RECAUDO.CONCEPTO       = DOBLESAGRUPSINUSUARIO.CONCEPTO)
  AND (SP_D_RECAUDO.NUMEROPAQUETE = DOBLESAGRUPSINUSUARIO.NUMEROPAQUETE)
  AND (SP_D_RECAUDO.BANCO         = DOBLESAGRUPSINUSUARIO.BANCO)
  AND (SP_D_RECAUDO.FECHA         = DOBLESAGRUPSINUSUARIO.FECHA)
  AND (SP_D_RECAUDO.COMPANIA      = DOBLESAGRUPSINUSUARIO.COMPANIA))
  ON (SP_CONCEPTOS.CODIGO         = SP_D_RECAUDO.CONCEPTO)
  AND (SP_CONCEPTOS.COMPANIA      = SP_D_RECAUDO.COMPANIA)
  WHERE (SP_D_RECAUDO.COMPANIA    = s$compania$s
  AND ((SP_D_RECAUDO.FECHA) BETWEEN s$fechaInicial$s AND s$fechaFinal$s)
  AND ((SP_CONCEPTOS.CODIGO) BETWEEN 1 AND 49
  OR (SP_CONCEPTOS.CODIGO) IN (201,202,203,204,205,206,207,246,247,248,249)))
  GROUP BY SP_CONCEPTOS.COMPANIA,
    SP_CONCEPTOS.CODIGO,
    SP_D_RECAUDO.BANCO
  ),
  CNSTOTALABONOSCONCEPTOBANCO AS
  (SELECT SP_ABONOS.COMPANIA,
    SP_D_ABONOS.CONCEPTO,
    SP_ABONOS.BANCO,
    SUM(SP_D_ABONOS.VALORACT) SUMADEVALORACT,
    SUM(SP_D_ABONOS.VALORANT) SUMADEVALORANT
  FROM SP_ABONOS
  LEFT JOIN SP_D_ABONOS
  ON (SP_ABONOS.CONSECUTIVO = SP_D_ABONOS.CONSECUTIVO)
  AND (SP_ABONOS.COMPANIA   = SP_D_ABONOS.COMPANIA)
  AND (SP_ABONOS.CICLO      = SP_D_ABONOS.CICLO)
  AND (SP_ABONOS.CODIGORUTA = SP_D_ABONOS.CODIGORUTA)
  AND (SP_ABONOS.ANO        = SP_D_ABONOS.ANO)
  AND (SP_ABONOS.PERIODO    = SP_D_ABONOS.PERIODO)
  WHERE SP_ABONOS.COMPANIA  = s$compania$s
  AND SP_ABONOS.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  GROUP BY SP_ABONOS.COMPANIA,
    SP_D_ABONOS.CONCEPTO,
    SP_ABONOS.BANCO
  )
SELECT CNSRECAUDOPAGOSPERIODOBANCO.CODIGO,
  SP_CONCEPTOS.NOMBRE AS NOMBRECONCEPTO,
  NVL(CNSTOTALABONOSCONCEPTOBANCO.SUMADEVALORACT,0) SUMADEVALORACT,
  NVL(CNSTOTALABONOSCONCEPTOBANCO.SUMADEVALORANT,0) SUMADEVALORANT,
  CNSRECAUDOPAGOSPERIODOBANCO.SUMADEVALORPAGOPERIODO,
  CNSRECAUDOPAGOSPERIODOBANCO.SUMADEVALORDEUDA,
  CNSRECAUDOPAGOSPERIODOBANCO.DBLTOTALES,
  NVL(SUMADEVALORACT,0)+NVL(SUMADEVALORANT,0)+NVL(SUMADEVALORPAGOPERIODO,0)+NVL(SUMADEVALORDEUDA,0) TOTALREC,
  CNSRECAUDOPAGOSPERIODOBANCO.BANCO,
  SP_BANCOS.NOMBRE NOMBREBANCO,
  CASE
    WHEN(NVL(CNSRECAUDOPAGOSPERIODOBANCO.BANCO,'')) =''
    THEN CNSTOTALABONOSCONCEPTOBANCO.BANCO
    ELSE CNSRECAUDOPAGOSPERIODOBANCO.BANCO
  END BANCOUNIFICADO
FROM ((SP_BANCOS
LEFT JOIN CNSRECAUDOPAGOSPERIODOBANCO
ON (SP_BANCOS.COMPANIA = CNSRECAUDOPAGOSPERIODOBANCO.COMPANIA)
AND (SP_BANCOS.CODIGO  = CNSRECAUDOPAGOSPERIODOBANCO.BANCO))
LEFT JOIN SP_CONCEPTOS
ON (CNSRECAUDOPAGOSPERIODOBANCO.CODIGO    = SP_CONCEPTOS.CODIGO)
AND (CNSRECAUDOPAGOSPERIODOBANCO.COMPANIA = SP_CONCEPTOS.COMPANIA))
LEFT JOIN CNSTOTALABONOSCONCEPTOBANCO
ON (CNSRECAUDOPAGOSPERIODOBANCO.BANCO     = CNSTOTALABONOSCONCEPTOBANCO.BANCO)
AND (CNSRECAUDOPAGOSPERIODOBANCO.CODIGO   = CNSTOTALABONOSCONCEPTOBANCO.CONCEPTO)
AND (CNSRECAUDOPAGOSPERIODOBANCO.COMPANIA = CNSTOTALABONOSCONCEPTOBANCO.COMPANIA)
WHERE ((CNSRECAUDOPAGOSPERIODOBANCO.CODIGO BETWEEN 1 AND 49
OR CNSRECAUDOPAGOSPERIODOBANCO.CODIGO       IN (201,202,203,204,205,206,207,246,247,248,249))
AND CNSRECAUDOPAGOSPERIODOBANCO.TOTALRECAUDO<>0
AND CNSRECAUDOPAGOSPERIODOBANCO.BANCO BETWEEN s$bancoInicial$s AND s$bancoFinal$s)
OR ((CNSRECAUDOPAGOSPERIODOBANCO.CODIGO BETWEEN 1 AND 49
OR CNSRECAUDOPAGOSPERIODOBANCO.CODIGO                 IN (201,202,203,204,205,206,207,246,247,248,249))
AND SUMADEVALORACT                     +SUMADEVALORANT<>0
AND CNSRECAUDOPAGOSPERIODOBANCO.BANCO BETWEEN s$bancoInicial$s AND s$bancoFinal$s)
