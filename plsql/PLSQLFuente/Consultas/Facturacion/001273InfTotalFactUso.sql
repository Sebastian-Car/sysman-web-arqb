SELECT QRY_TOTALFACTUSO.CODIGOINTERNO INTERNO,
  QRY_TOTALFACTUSO.NIT,
  QRY_TOTALFACTUSO.USO,
  QRY_TOTALFACTUSO.NOMBRES,
  QRY_TOTALFACTUSO.DIRCORRESPONDENCIA,
  QRY_TOTALFACTUSO.TELEFONOS,
  QRY_TOTALFACTUSO.TOTAL+QRY_TOTALFACTHUSO.SUMADETOTFACTURAPERACTUAL AS SUMATOTAL,
  QRY_TOTALFACTUSO.E_MAIL,
  QRY_TOTALFACTUSO.CODIGO,
  QRY_TOTALFACTUSO.NITCOMPANIA,
  QRY_TOTALFACTUSO.NUIR,
  QRY_TOTALFACTUSO.NOMBRE,
  QRY_TOTALFACTUSO.TELEFONO,
  QRY_TOTALFACTUSO.DIRECCION,
  1 AS GRUPO
FROM (
  SELECT SP_HISTORIA.CODIGOINTERNO,
    SP_USUARIO.NIT,
    SP_USOS.NOMBRE AS USO,
    (SP_USUARIO.NOMBRES || ' ' || SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO) AS NOMBRES,
    SP_USUARIO.DIRCORRESPONDENCIA,
    NVL(SP_USUARIO.TELEFONO,'')  AS TELEFONOS,
    SUM(SP_HISTORIA.TOTFACTURAPERACTUAL) AS SumaDeTOTFACTURAPERACTUAL,
    NVL(SP_USUARIO.EMAIL,'') AS E_MAIL,
    SP_HISTORIA.COMPANIA,
    COMPANIA.NITCOMPANIA NITCOMPANIA,
    COMPANIA.NITCOMPANIA NUIR,
    COMPANIA.NOMBRE,
    COMPANIA.TELEFONO,
    COMPANIA.DIRECCION
  FROM SP_HISTORIA
    INNER JOIN SP_USUARIO
      ON  SP_HISTORIA.CODIGORUTA = SP_USUARIO.CODIGORUTA
      AND SP_HISTORIA.CICLO     = SP_USUARIO.CICLO
      AND SP_HISTORIA.COMPANIA  = SP_USUARIO.COMPANIA
    INNER JOIN SP_USOS
      ON  SP_HISTORIA.COMPANIA = SP_USOS.COMPANIA
      AND SP_HISTORIA.USO     = SP_USOS.CODIGO
    INNER JOIN COMPANIA 
      ON SP_USOS.COMPANIA      = COMPANIA.CODIGO
  WHERE SP_HISTORIA.COMPANIA = s$compania$s
    AND SP_HISTORIA.CICLO BETWEEN 's$cicloInicial$s' AND 's$cicloFinal$s'
    AND SP_HISTORIA.ANO    = s$ano$s
  GROUP BY SP_HISTORIA.CODIGOINTERNO,
    SP_USUARIO.NIT,
    SP_USOS.NOMBRE,
    (SP_USUARIO.NOMBRES || ' ' || SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO),
    SP_USUARIO.DIRCORRESPONDENCIA,
    SP_USUARIO. TELEFONO,
    SP_USUARIO.EMAIL,
    SP_HISTORIA.COMPANIA,
    COMPANIA.NITCOMPANIA,
    COMPANIA.NOMBRE,
    COMPANIA.TELEFONO,
    COMPANIA.DIRECCION)QRY_TOTALFACTHUSO
  INNER JOIN (
    SELECT SP_USUARIO.CODIGOINTERNO,
    SP_USUARIO.NIT,
    SP_USOS.NOMBRE AS USO,
    (SP_USUARIO.NOMBRES || ' ' || SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO) AS NOMBRES,
    SP_USUARIO.DIRCORRESPONDENCIA,
    NVL(SP_USUARIO.TELEFONO,'')      AS TELEFONOS,
    SUM(SP_FACTURADO.VALOR_FACTURADO) AS TOTAL,
    NVL(SP_USUARIO.EMAIL,'')AS E_MAIL,
    COMPANIA.CODIGO,
    COMPANIA.NITCOMPANIA AS NITCOMPANIA,
    COMPANIA.NITCOMPANIA AS NUIR,
    COMPANIA.NOMBRE,
    COMPANIA.TELEFONO,
    COMPANIA.DIRECCION
  FROM SP_FACTURADO
    INNER JOIN SP_USUARIO
      ON  SP_FACTURADO.COMPANIA    = SP_USUARIO.COMPANIA
      AND SP_FACTURADO.CICLO      = SP_USUARIO.CICLO
      AND SP_FACTURADO.CODIGORUTA = SP_USUARIO.CODIGORUTA
    INNER JOIN COMPANIA
      ON  SP_USUARIO.COMPANIA = COMPANIA.CODIGO
    INNER JOIN SP_USOS
      ON  SP_USUARIO.COMPANIA  = SP_USOS.COMPANIA
      AND SP_USUARIO.USO        = SP_USOS.CODIGO
  WHERE SP_FACTURADO.COMPANIA = s$compania$s
    AND SP_FACTURADO.CICLO BETWEEN 's$cicloInicial$s' AND 's$cicloFinal$s'
    AND SP_FACTURADO.ANO = s$ano$s
  GROUP BY SP_USUARIO.CODIGOINTERNO,
    SP_USUARIO.NIT,
    SP_USOS.NOMBRE,
    (SP_USUARIO.NOMBRES || ' ' || SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO),
    SP_USUARIO.DIRCORRESPONDENCIA,
    COMPANIA.CODIGO,
    COMPANIA.NITCOMPANIA,
    COMPANIA.NOMBRE,
    COMPANIA.TELEFONO,
    COMPANIA.DIRECCION,
    SP_USUARIO.TELEFONO,
    SP_USUARIO.EMAIL )QRY_TOTALFACTUSO
  ON  QRY_TOTALFACTHUSO.COMPANIA  = QRY_TOTALFACTUSO.CODIGO
  AND QRY_TOTALFACTHUSO.CODIGOINTERNO = QRY_TOTALFACTUSO.CODIGOINTERNO
  AND QRY_TOTALFACTHUSO.NIT  = QRY_TOTALFACTUSO.NIT
  AND QRY_TOTALFACTHUSO.USO = QRY_TOTALFACTUSO.USO
GROUP BY QRY_TOTALFACTUSO.CODIGOINTERNO,
  QRY_TOTALFACTUSO.NIT,
  QRY_TOTALFACTUSO.USO,
  QRY_TOTALFACTUSO.NOMBRES,
  QRY_TOTALFACTUSO.DIRCORRESPONDENCIA,
  QRY_TOTALFACTUSO.TELEFONOS,
  QRY_TOTALFACTUSO.TOTAL+QRY_TOTALFACTHUSO.SUMADETOTFACTURAPERACTUAL,
  QRY_TOTALFACTUSO.E_MAIL,
  QRY_TOTALFACTUSO.CODIGO,
  QRY_TOTALFACTUSO.NIT,
  QRY_TOTALFACTUSO.NUIR,
  QRY_TOTALFACTUSO.NOMBRE,
  QRY_TOTALFACTUSO.TELEFONO,
  QRY_TOTALFACTUSO.DIRECCION
