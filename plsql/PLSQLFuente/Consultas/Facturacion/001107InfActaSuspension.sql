SELECT DISTINCT SP_ACTASDESUSPENSION.COMPANIA, 
                SP_ACTASDESUSPENSION.CODIGORUTA, 
                TO_CHAR(SP_ACTASDESUSPENSION.FECHAACTA, 'DD/MM/YYYY') AS FECHAACTA, 
                (SP_USUARIO.PRIMERAPELLIDO || ' ' || SP_USUARIO.SEGUNDOAPELLIDO || ' ' || SP_USUARIO.NOMBRES) AS NOMBREUSUARIO, 
                SP_USUARIO.DIRTECNICA, 
                SP_USUARIO.DIRCORRESPONDENCIA, 
                SP_USUARIO.DIRGUIA, 
                SP_USUARIO.MEDIDOR, 
                SP_USUARIO.TOTFACTURAPERACTUAL, 
                SP_USUARIO.PERIODOSATRASO, 
                SP_USUARIO.CODIGOINTERNO, 
                SP_USUARIO.USO, 
                SP_USUARIO.ESTRATO, 
                SP_ACTASDESUSPENSION.PERIODO, 
                SP_ACTASDESUSPENSION.ANO, 
                SP_ACTASDESUSPENSION.IDACTA, 
                COMPANIA.SIGLACOMPANIA, 
                COMPANIA.NOMBRE, 
                COMPANIA.RUTA_VIGILADO_POR AS VIGILADOPOR, 
                COMPANIA.DIRECCION, 
                COMPANIA.TELEFONO, 
                COMPANIA.NITCOMPANIA AS NIT, 
                SP_USUARIO.LECTURA, 
                SP_USUARIO.LECTURA1 
FROM  (SP_ACTASDESUSPENSION 
      INNER JOIN SP_USUARIO 
        ON  SP_ACTASDESUSPENSION.CODIGORUTA  = SP_USUARIO.CODIGORUTA 
        AND SP_ACTASDESUSPENSION.COMPANIA    = SP_USUARIO.COMPANIA 
        AND SP_ACTASDESUSPENSION.CICLO       = SP_USUARIO.CICLO) 
          INNER JOIN COMPANIA 
            ON SP_ACTASDESUSPENSION.COMPANIA = COMPANIA.CODIGO 
WHERE SP_ACTASDESUSPENSION.COMPANIA    = s$compania$s 
s$ciclo$s 
AND   SP_ACTASDESUSPENSION.PERIODO     = 's$periodo$s' 
AND   SP_ACTASDESUSPENSION.ANO         = s$ano$s 
ORDER BY  SP_ACTASDESUSPENSION.COMPANIA, 
          SP_ACTASDESUSPENSION.CODIGORUTA, 
          SP_ACTASDESUSPENSION.PERIODO, 
          SP_ACTASDESUSPENSION.ANO
