SELECT
ESTADISTICAS_CONSUMO.ESTRATO,
ESTADISTICAS_CONSUMO.USO,
ESTADISTICAS_CONSUMO.RANGO,
ESTADISTICAS_CONSUMO.SUMAUSUARIOSPROBLEMA,
ESTADISTICAS_CONSUMO.CONSUMOPROMEDIO,
ESTADISTICAS_CONSUMO.CONSUMO,
ESTADISTICAS_CONSUMO.VCONSUMOACUEDUCTO,
ESTADISTICAS_CONSUMO.CARGOFIJOACUEDUCTO,
ESTADISTICAS_CONSUMO.FACTURADOPROMEDIO,
ESTADISTICAS_CONSUMO.COMPANIA,
ESTADISTICAS_CONSUMO.ANO,
ESTADISTICAS_CONSUMO.PERIODO,
ESTADISTICAS_CONSUMO.CANTUSUARIOSACUEDUCTO,
  PCK_SYSMAN_UTL.FC_STRZERO(ANO,4)|| PCK_SYSMAN_UTL.FC_STRZERO(PERIODO,2) AS PERIODOUNICO,
  ESTRATOS.CODIGO||' - '|| ESTRATOS.NOMBRE        AS NOMBREESTRATO,
  USOS.CODIGO||' - '|| USOS.NOMBRE  AS NOMBRE_USO,        
 USOS.NOMBRE AS NOMBREUSO  ,                                                                                                                                                              
  RANGO.LIMITEINFERIOR|| ' - '|| RANGO.LIMITESUPERIOR AS DESCRANGO
FROM ((SP_ESTADISTICAS_CONSUMO ESTADISTICAS_CONSUMO
LEFT JOIN SP_ESTRATOS ESTRATOS
ON (ESTADISTICAS_CONSUMO.COMPANIA = ESTRATOS.COMPANIA)
AND (ESTADISTICAS_CONSUMO.USO     = ESTRATOS.USO)
AND (ESTADISTICAS_CONSUMO.ESTRATO = ESTRATOS.CODIGO))
LEFT JOIN SP_USOS USOS
ON (ESTADISTICAS_CONSUMO.USO       = USOS.CODIGO)
AND (ESTADISTICAS_CONSUMO.COMPANIA = USOS.COMPANIA))
LEFT JOIN SP_RANGO RANGO
ON (ESTADISTICAS_CONSUMO.COMPANIA = RANGO.COMPANIA)
AND (ESTADISTICAS_CONSUMO.RANGO   = RANGO.CODIGO)
WHERE
PCK_SYSMAN_UTL.FC_STRZERO(Ano,4)|| PCK_SYSMAN_UTL.FC_STRZERO(periodo,2)  BETWEEN  PCK_SYSMAN_UTL.FC_STRZERO(s$anoInicial$s,4)|| PCK_SYSMAN_UTL.FC_STRZERO(s$periodoInicial$s,2)
AND PCK_SYSMAN_UTL.FC_STRZERO(s$anoFinal$s,4)|| PCK_SYSMAN_UTL.FC_STRZERO(s$periodoFinal$s,2)
AND ESTADISTICAS_CONSUMO.COMPANIA=s$compania$s
AND ESTADISTICAS_CONSUMO.SERVICIO='01'
s$cicloCond$s
