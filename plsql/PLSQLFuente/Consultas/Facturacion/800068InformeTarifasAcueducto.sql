WITH C1 AS (SELECT * FROM (
SELECT Tarifas.COMPANIA,
  Tarifas.USOSUPERSERVICIOS,
  CASE WHEN USOSUPERSERVICIOS='1'
       THEN  ESTRATO
       ELSE '1'
       END AS estrat,
  CASE WHEN USOSUPERSERVICIOS='1'
       THEN '00'
       ELSE 'ZZ'
       END   AS RANGO,
  ANO||PCK_SYSMAN_UTL.FC_STRZERO(PERIODO,2) AS PERIOCONC, 
  TARIFAS.FIJOALCANTARILLADO
FROM SP_TARIFAS TARIFAS
WHERE TARIFAS.COMPANIA              =s$compania$s
  AND TARIFAS.USOSUPERSERVICIOS IS NOT NULL
  AND (ANO||PCK_SYSMAN_UTL.FC_STRZERO(PERIODO,2) BETWEEN PCK_SYSMAN_UTL.FC_STRZERO('s$anoInicial$s',4)||PCK_SYSMAN_UTL.FC_STRZERO('s$periodoInicial$s',2) 
                                                                                AND PCK_SYSMAN_UTL.FC_STRZERO('s$anoFinal$s',4)||PCK_SYSMAN_UTL.FC_STRZERO('s$periodoFinal$s',2))
  AND TARIFAS.USO<>'99'
GROUP BY TARIFAS.COMPANIA, 
  TARIFAS.USOSUPERSERVICIOS, 
  CASE WHEN USOSUPERSERVICIOS='1'
       THEN  ESTRATO
       ELSE '1'
       END, 
  CASE WHEN USOSUPERSERVICIOS='1'
       THEN '00'
       ELSE 'ZZ'
       END,
  ANO||PCK_SYSMAN_UTL.FC_STRZERO(periodo,2),
  Tarifas.FIJOALCANTARILLADO
ORDER BY TARIFAS.COMPANIA,
  TARIFAS.USOSUPERSERVICIOS,3
) PIVOT (MIN(FIJOALCANTARILLADO)  FOR PERIOCONC IN (s$inPivot$s)  )
ORDER BY 2,3,4),

