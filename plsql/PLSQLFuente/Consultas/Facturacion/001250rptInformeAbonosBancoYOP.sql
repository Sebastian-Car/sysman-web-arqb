SELECT
ABONOS.BANCO,
 ABONOS.PERIODO,
  ABONOS.ANO||ABONOS.PERIODO NOMBREDEPERIODO,
  PRIMERAPELLIDO||' '||SEGUNDOAPELLIDO||' '||NOMBRES    NOMBRECOMPLETO,
  ABONOS.VALOR VLRTOTAL,
  PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(abonos.compania,abonos.ano,abonos.periodo,NULL) TXTNOMPERIODO,
 TO_CHAR( ABONOS.FECHA,'DD/MM/YYYY')  FECHAABONO,
  BANCOS.NOMBRE NOMBREBANCO,
NVL(SUMAABONOS.ABONOACU,0) ABONOACU,
 NVL( SUMAABONOS.ABONOALC,0) ABONOALC,
  NVL(SUMAABONOS.ABONOASE,0) ABONOASE,
 USUARIO.CODIGOINTERNO
FROM (SP_ABONOS ABONOS
INNER JOIN SP_USUARIO USUARIO
ON (ABONOS.CODIGORUTA = USUARIO.CODIGORUTA)
AND (ABONOS.CICLO     = USUARIO.CICLO)
AND (ABONOS.COMPANIA  = USUARIO.COMPANIA))
INNER JOIN SP_BANCOS BANCOS
ON (ABONOS.COMPANIA  = BANCOS.COMPANIA)
AND (ABONOS.BANCO    = BANCOS.CODIGO)
LEFT JOIN (
SELECT SUM(CASE WHEN SERVICIO ='01'
                THEN D_ABONOS.VALOR
                ELSE 0
                END)    ABONOACU,
  SUM(CASE WHEN SERVICIO ='02' 
           THEN D_ABONOS.VALOR
           ELSE 0
           END) ABONOALC,
  SUM(CASE WHEN SERVICIO    ='03' 
           THEN D_ABONOS.VALOR
           ELSE 0
           END)  ABONOASE ,
           D_ABONOS.PERIODO,
           D_ABONOS.ANO,
           D_ABONOS.CODIGORUTA,
           D_ABONOS.CICLO,
           D_ABONOS.COMPANIA
FROM SP_CONCEPTOS CONCEPTOS
  INNER JOIN SP_D_ABONOS D_ABONOS
    ON  CONCEPTOS.COMPANIA = D_ABONOS.COMPANIA
    AND CONCEPTOS.CODIGO    = D_ABONOS.CONCEPTO

GROUP BY D_ABONOS.COMPANIA,
  D_ABONOS.CICLO,
  D_ABONOS.CODIGORUTA,
  D_ABONOS.ANO,
  D_ABONOS.PERIODO
)SUMAABONOS ON SUMAABONOS.Compania =  ABONOS.COMPANIA
AND SUMAABONOS.Ciclo      = ABONOS.CICLO 
AND SUMAABONOS.CODIGORUTA = ABONOS.CODIGORUTA
AND SUMAABONOS.ANO        = ABONOS.ANO
AND SUMAABONOS.PERIODO    = ABONOS.PERIODO
WHERE ABONOS.COMPANIA=s$compania$s
AND  TO_DATE(TO_CHAR(ABONOS.FECHA,'DD/MM/YYYY'),'DD/MM/YYYY') BETWEEN  TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND  TO_DATE('s$fechaFinal$s'
,'DD/MM/YYYY')
s$ciclo$s
s$codigo$s
