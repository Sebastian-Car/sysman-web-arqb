SELECT 	SP_USUARIO.CICLO,
        SP_USUARIO.CODIGORUTA,
        PCK_SERVICIOS_PUBLICOS.FC_NOMBREPERIODO(SP_USUARIO.COMPANIA, SP_USUARIO.ANO, SP_USUARIO.PERIODO, NULL)NOMBREPERIODO,
        SP_USUARIO.PRIMERAPELLIDO ||  ' ' || SP_USUARIO.SEGUNDOAPELLIDO ||  ' ' || SP_USUARIO.NOMBRES NOMBREUSUARIO, 
        SP_USUARIO.DIRTECNICA,
        SP_USUARIO.LECTURA,
        SP_USUARIO.PERIODOSATRASO,
        CASE WHEN SP_USUARIO.BANCOPERPROCESO IS NOT NULL THEN 0 ELSE SP_USUARIO.TOTFACTURAPERACTUAL END VALORPAGO,
        NVL(SALDOFINDEUDAYSALDOOTROS.SALDOFINDEUDA,0) SALDOFINDEUDA,
        NVL(SALDOFINDEUDAYSALDOOTROS.SALDOFINPERIODO,0) SALDOFINPERIODO,
        NVL(SALDOFINDEUDAYSALDOOTROS.SALDOFINOTROS,0) SALDOFINOTROS,
        CASE WHEN SP_USUARIO.BANCOPERPROCESO IS NOT NULL THEN 0 ELSE SP_USUARIO.TOTFACTURAPERACTUAL END + NVL(SALDOFINDEUDAYSALDOOTROS.SALDOFINDEUDA,0)+NVL(SALDOFINDEUDAYSALDOOTROS.SALDOFINPERIODO,0)+NVL(SALDOFINDEUDAYSALDOOTROS.SALDOFINOTROS,0) TOTAL_CARTERA,
        DECODE(SP_USUARIO.CHAPETAS,-1,'X', 0,' ')CHAPETAS
FROM SP_USUARIO
  LEFT JOIN (SELECT  SP_USUARIO.COMPANIA,
                     SP_USUARIO.CICLO,
                     SP_USUARIO.CODIGORUTA,
                     SP_USUARIO.ANO,
                     SP_USUARIO.PERIODO,
                     SP_USUARIO.USO,
                     SP_USUARIO.ESTRATO,
                     SUM(NVL(SP_D_DEUDAFACTURADAFINANCIADA.SALDOFINANT,0)) AS SALDOFINDEUDA,
                     SUM(NVL(SP_D_DEUDAFACTURADAFINANCIADA.SALDOFINACT,0)) AS SALDOFINPERIODO,
                     SUM(0*5)                 AS SALDOFINOTROS
             FROM SP_USUARIO
               INNER JOIN SP_FACTURADO
                 ON SP_USUARIO.COMPANIA  = SP_FACTURADO.COMPANIA 
                 AND SP_USUARIO.CODIGORUTA = SP_FACTURADO.CODIGORUTA
                 AND SP_USUARIO.CICLO     = SP_FACTURADO.CICLO
               INNER JOIN SP_D_DEUDAFACTURADAFINANCIADA
                 ON  SP_FACTURADO.COMPANIA    = SP_D_DEUDAFACTURADAFINANCIADA.COMPANIA 
                 AND SP_FACTURADO.CICLO       = SP_D_DEUDAFACTURADAFINANCIADA.CICLO
                 AND SP_FACTURADO.CODIGORUTA  = SP_D_DEUDAFACTURADAFINANCIADA.CODIGORUTA
                 AND SP_FACTURADO.ANO         = SP_D_DEUDAFACTURADAFINANCIADA.ANO
                 AND SP_FACTURADO.PERIODO     = SP_D_DEUDAFACTURADAFINANCIADA.PERIODO
                 AND SP_FACTURADO.CONCEPTO    = SP_D_DEUDAFACTURADAFINANCIADA.CONCEPTO
             WHERE  SP_FACTURADO.CONCEPTO NOT IN(12)
             GROUP BY  SP_USUARIO.COMPANIA,
                       SP_USUARIO.CICLO,
                       SP_USUARIO.CODIGORUTA,
                       SP_USUARIO.ANO,
                       SP_USUARIO.PERIODO,
                       SP_USUARIO.USO,
                       SP_USUARIO.ESTRATO
             HAVING SUM(NVL(SP_D_DEUDAFACTURADAFINANCIADA.SALDOFINACT,0)+NVL(SP_D_DEUDAFACTURADAFINANCIADA.SALDOFINANT,0))<>0
             UNION
             SELECT 	SP_USUARIO.COMPANIA,
                      SP_USUARIO.CICLO,
                      SP_USUARIO.CODIGORUTA,
                      SP_USUARIO.ANO,
                      SP_USUARIO.PERIODO,
                      SP_USUARIO.USO,
                      SP_USUARIO.ESTRATO,
                      SUM(5*0)   AS SALDOFINDEUDA,
                      SUM(0*5)   AS SALDOFINPERIODO,
                      SUM(NVL(SP_FINANCIABLES.SALDOFINANCIABLE,0)-NVL(SP_FACTURADO.VALOR_FACTURADO,0)) AS SALDOFINOTROS
              FROM SP_USUARIO
                INNER JOIN SP_FACTURADO
                  ON  SP_USUARIO.COMPANIA    = SP_FACTURADO.COMPANIA 
                  AND SP_USUARIO.CICLO       = SP_FACTURADO.CICLO
                  AND SP_USUARIO.CODIGORUTA  = SP_FACTURADO.CODIGORUTA
                INNER JOIN  SP_FINANCIABLES 
                  ON SP_FACTURADO.COMPANIA     = SP_FINANCIABLES.COMPANIA 
                  AND SP_FACTURADO.CICLO       = SP_FINANCIABLES.CICLO
                  AND SP_FACTURADO.CODIGORUTA  = SP_FINANCIABLES.CODIGORUTA
                  AND SP_FACTURADO.ANO         = SP_FINANCIABLES.ANO
                  AND SP_FACTURADO.PERIODO     = SP_FINANCIABLES.PERIODO
                  AND SP_FACTURADO.CONCEPTO    = SP_FINANCIABLES.CONCEPTO	
              WHERE  SP_FACTURADO.CONCEPTO NOT IN(12) 
              GROUP BY 	SP_USUARIO.COMPANIA,
                    SP_USUARIO.CICLO,
                    SP_USUARIO.CODIGORUTA,
                    SP_USUARIO.ANO,
                    SP_USUARIO.PERIODO,
                    SP_USUARIO.USO,
                    SP_USUARIO.ESTRATO
              HAVING SUM(NVL(SP_FINANCIABLES.SALDOFINANCIABLE,0)-NVL(SP_FACTURADO.VALOR_FACTURADO,0))<>0) SALDOFINDEUDAYSALDOOTROS
    ON SP_USUARIO.COMPANIA    = SALDOFINDEUDAYSALDOOTROS.COMPANIA 
    AND SP_USUARIO.CICLO      = SALDOFINDEUDAYSALDOOTROS.CICLO
    AND SP_USUARIO.CODIGORUTA = SALDOFINDEUDAYSALDOOTROS.CODIGORUTA
    AND SP_USUARIO.ANO        = SALDOFINDEUDAYSALDOOTROS.ANO 
    AND SP_USUARIO.PERIODO    = SALDOFINDEUDAYSALDOOTROS.PERIODO
    AND SP_USUARIO.ESTRATO    = SALDOFINDEUDAYSALDOOTROS.ESTRATO 
    AND SP_USUARIO.USO        = SALDOFINDEUDAYSALDOOTROS.USO
WHERE SP_USUARIO.COMPANIA = s$compania$s
  AND SP_USUARIO.CICLO BETWEEN s$cicloInicial$s AND s$cicloFinal$s 
  AND SP_USUARIO.USO   BETWEEN s$usoInicial$s AND s$usoFinal$s 
  AND SP_USUARIO.ESTRATO BETWEEN s$estratoInicial$s AND s$estratoFinal$s 
  AND ((CASE WHEN SP_USUARIO.BANCOPERPROCESO IS NOT NULL THEN 0 ELSE SP_USUARIO.TOTFACTURAPERACTUAL END) + 
          (NVL(SALDOFINDEUDAYSALDOOTROS.SALDOFINDEUDA,0)+
           NVL(SALDOFINDEUDAYSALDOOTROS.SALDOFINPERIODO,0)+
           NVL(SALDOFINDEUDAYSALDOOTROS.SALDOFINOTROS,0)))> s$valoresSuperiores$s
  AND SP_USUARIO.PERIODOSATRASO BETWEEN s$atrasoInicial$s AND s$atrasoFinal$s 
  s$condicionChapetas$s
  s$condicionBarrio$s
  AND SP_USUARIO.ESTADO NOT IN ('S','R')
  s$orderBy$s
