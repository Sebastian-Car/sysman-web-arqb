SELECT  TRANSACCION.TIPOCONTRATO || ' - ' || TIPOCONTRATO_PR.DESCRIPCION AS MODALIDADDESELECCION,  
        LPAD(TRANSACCION.CONSECUTIVO,8,'0') CONSECUTIVO,  
       TO_CHAR(TRANSACCION.FECHA ,'DD "de" month "de" YYYY','nls_date_language=spanish') AS FECHA, 
        TRANSACCION.ANO AS AÑO,
        DECODE(ESTADO,'CE','Cerrado','AC','Activo','Cancelado') ESTADODELPROCESO,  
        TRANSACCION.NUM_PROCESO AS NUMERODELPROCESO,  
        TRANSACCION.ESTUDIOPREVIO AS NUMERODELESTUDIOPREVIO 
FROM TRANSACCION  
  INNER JOIN TIPOCONTRATO_PR  
    ON TRANSACCION.COMPANIA = TIPOCONTRATO_PR.COMPANIA  
    AND TRANSACCION.TIPOCONTRATO = TIPOCONTRATO_PR.TIPOCONTRATO 
  LEFT JOIN ES_TIPOCONTRATO 
  ON TIPOCONTRATO_PR.COMPANIA = ES_TIPOCONTRATO.COMPANIA 
  AND TIPOCONTRATO_PR.TIPOCONTRATO = ES_TIPOCONTRATO.CODIGOCONTRATO 
WHERE TRANSACCION.COMPANIA= s$compania$s 
  AND ES_TIPOCONTRATO.COD_T_CONTRATO BETWEEN s$modalidadInicial$s AND s$modalidadFinal$s 
  AND TRANSACCION.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
 ORDER BY MODALIDADDESELECCION ASC,CONSECUTIVO ASC
