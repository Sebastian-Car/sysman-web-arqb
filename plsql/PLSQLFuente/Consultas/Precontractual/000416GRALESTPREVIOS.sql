SELECT ES_ESTPREVIO.COD_ESTUDIO AS NUMERODELESTUDIO,  
       ES_ESTPREVIO.NOMBRE_ESTUDIO AS NOMBREDELESTUDIO,  
       TERCERO.NOMBRE AS SOLICITANTE,  
       ES_TIPOCONTRATO.COD_T_CONTRATO,  
       ES_ESTPREVIO.TIPO_CONTRATO || ' - ' || ES_TIPOCONTRATO.NOMBRE AS MODALIDADDESELECCION,  
       TO_CHAR(ES_ESTPREVIO.FECHA , 'DD "de" month "de" YYYY','nls_date_language=spanish') AS FECHA,  
       ES_ESTPREVIO.OBJETO_CONTRATO,  
       DEPENDENCIA.NOMBRE AS DEPENDENCIA,  
       TERCERO.NOMBRE AS RESPONSABLE,  
       CASE WHEN ES_ESTPREVIO.ESTADO = 0 THEN 'CERRADO' ELSE 'ACTIVO' END  AS ESTADO,  
       CASE WHEN ES_ESTPREVIO.VOBO = 0 THEN 'APROBADO' ELSE 'DESAPROBADA' END  AS VOBO,  
       ES_ESTPREVIO.CARGO_INTERVENTOR, 
       ES_ESTPREVIO.PLAZO_EJECUCION, 
       ES_ESTPREVIO.COSTOTOTALEST 
FROM ES_ESTPREVIO  
  LEFT JOIN RESPONSABLE
     ON ES_ESTPREVIO.COMPANIA = RESPONSABLE.COMPANIA  
    AND ES_ESTPREVIO.RESPONSABLE = RESPONSABLE.CEDULA 
    AND ES_ESTPREVIO.SUCURSAL_RESPONSABLE = RESPONSABLE.SUCURSAL 
  INNER JOIN ES_TIPOCONTRATO ON ES_ESTPREVIO.COMPANIA = ES_TIPOCONTRATO.COMPANIA  
    AND ES_ESTPREVIO.TIPO_CONTRATO = ES_TIPOCONTRATO.COD_T_CONTRATO  
  INNER JOIN DEPENDENCIA ON ES_ESTPREVIO.COMPANIA = DEPENDENCIA.COMPANIA  
    AND ES_ESTPREVIO.DEPENDENCIA = DEPENDENCIA.CODIGO 
  LEFT JOIN TERCERO ON RESPONSABLE.COMPANIA = TERCERO.COMPANIA 
    AND RESPONSABLE.CEDULA = TERCERO.NIT 
    AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL 
WHERE ES_ESTPREVIO.COMPANIA = s$compania$s  
  AND ES_TIPOCONTRATO.COD_T_CONTRATO BETWEEN 's$modalidadInicial$s' AND 's$modalidadFinal$s' 
  AND ES_ESTPREVIO.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
