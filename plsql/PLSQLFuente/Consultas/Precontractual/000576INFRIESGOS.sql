SELECT RE.COD_ESTUDIO, 
       EP.NOMBRE_ESTUDIO,
       TR.NOMBRE TIPORIESGO,
       RI.NOMBRE RIESGO,
       RE.FECHA_RECIBIDO FECHA_ANALISIS,  
       RESP.NOMBRE RESPONSABLE_ANALISIS, 
       RE.RESP_ENTREGA RESPONSABLE_APROBACION, 
       RE.IMPACTO,
       RE.ESTIMACION
FROM ES_RIES_ESTPR RE 
  INNER JOIN TERCERO RESP
    ON  RE.COMPANIA               = RESP.COMPANIA
    AND RE.RESP_RECIBIDO          = RESP.NIT
    AND RE.SUCURSAL_RESP_RECIBIDO = RESP.SUCURSAL
  INNER JOIN ES_ESTPREVIO EP
    ON  RE.COMPANIA               = EP.COMPANIA
    AND RE.COD_ESTUDIO            = EP.COD_ESTUDIO
  INNER JOIN ES_RIESGO RI
    ON  RE.COMPANIA               = RI.COMPANIA
    AND RE.COD_RIESGO             = RI.COD_RIESGO
    AND RE.COD_T_RIESGO           = RI.COD_T_RIESGO
  INNER JOIN ES_T_RIESGO TR 
    ON  RE.COMPANIA               = TR.COMPANIA
    AND RE.COD_T_RIESGO           = TR.COD_T_RIESGO
WHERE RE.COMPANIA = s$compania$s
  AND RE.COD_ESTUDIO BETWEEN s$estudioInicial$s AND s$estudioFinal$s
  AND EP.TIPO_DIA = 's$tipoEstudio$s'
