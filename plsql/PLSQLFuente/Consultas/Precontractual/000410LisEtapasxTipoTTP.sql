SELECT 
      TIPOCONTRATO_PR.TIPOCONTRATO TIPOCONTRATO,
      TIPOCONTRATO_PR.DESCRIPCION TIPODESCRIPCION,
      ETAPA_PR.IDETAPA,
      ETAPA_PR.DESCRIPCION ETAPADESCRIPCION,
      ETAPA_PR.DIAS, 
      DECODE(ETAPA_PR.TIPODIAS,1,'Calendario',2,'Habiles') TIPODIAS,
      ETAPA_PR.MODELO_DOC MODELODOC, 
      DECODE(ETAPA_PR.HEREDAPROPONENTE,0,'NO','SI')HEREDAPROPONENTE,
      DECODE(ETAPA_PR.HEREDABLE,0,'SI','NO') HEREDABLE, 
      DECODE(ETAPA_PR.COTIZA_ELEMENTOS,0,'NO','SI') COTIZAELEMENTOS
FROM TIPOCONTRATO_PR 
LEFT JOIN ETAPA_PR 
ON TIPOCONTRATO_PR.COMPANIA = ETAPA_PR.COMPANIA 
AND TIPOCONTRATO_PR.TIPOCONTRATO = ETAPA_PR.TIPOCONTRATO
WHERE TIPOCONTRATO_PR.COMPANIA= s$compania$s
AND TIPOCONTRATO_PR.TIPOCONTRATO = 's$tipoContrato$s'
AND ETAPA_PR.IDETAPA Between s$etapaInicial$s And s$etapaFinal$s
