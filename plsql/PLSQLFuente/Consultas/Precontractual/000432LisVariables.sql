SELECT VARIABLE.TIPOCONTRATO,  
       TIPOCONTRATO_PR.DESCRIPCION AS DESCRIPCION,  
       VARIABLE.CODIGO,  
       VARIABLE.DESCRIPCION AS DESCRIPCIONVARIABLE,  
       DECODE (VARIABLE.TIPO , '1' , 'Texto' , '2','Memo' , '3' , 'Numérico' , '4','Fecha','5','Formula','6','Otro') AS TIPO,  
       VARIABLE.VRPREDETERMINADO,  
       DECODE (VARIABLE.ESCONSTANTE, '-1','Si','0','No') AS ESCONSTANTE,  
       VARIABLE.ORDEN 
FROM TIPOCONTRATO_PR  
  LEFT JOIN VARIABLE  
    ON  TIPOCONTRATO_PR.COMPANIA = VARIABLE.COMPANIA 
    AND TIPOCONTRATO_PR.TIPOCONTRATO = VARIABLE.TIPOCONTRATO  
WHERE TIPOCONTRATO_PR.COMPANIA =  s$compania$s 
  AND VARIABLE.TIPOCONTRATO =  s$tipoContrato$s 
  AND VARIABLE.CODIGO BETWEEN  s$variableInicial$s AND  s$variableFinal$s 
ORDER BY VARIABLE.CODIGO, VARIABLE.ORDEN
