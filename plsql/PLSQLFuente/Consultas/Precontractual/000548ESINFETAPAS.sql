SELECT ES_ETAPA.NOMBRE TIPO_ETAPA, 
ES_D_ETAPA.DESCRIPCION ETAPA, 
TO_CHAR(ES_DETA_ESTPR.FECHA_RECIBIDO,'DD/MM/YYYY') FECHA_RECIBIDO,  
TO_CHAR(ES_DETA_ESTPR.FECHA_ENTREGA,'DD/MM/YYYY') FECHA_ENTREGA, 
ES_DETA_ESTPR.RESP_ENTREGA R_ENTREGA, 
ES_DETA_ESTPR.RESP_RECIBIDO R_RECIBIDO,
CASE WHEN ES_DETA_ESTPR.REALIZADO = 0 THEN 'NO' ELSE 'SI' END REALIZADO,
CASE WHEN ES_DETA_ESTPR.VERIFICADO = 0 THEN 'NO' ELSE 'SI' END VERIFICADO,
ES_DETA_ESTPR.OBSERVACIONES,
ES_ESTPREVIO.COD_ESTUDIO,
ES_ESTPREVIO.NOMBRE_ESTUDIO ESTUDIO, 
ES_DETA_ESTPR.COMPANIA, 
TER_ENTREGA.NOMBRE RESP_ENTREGA, 
TER_RECIBIDO.NOMBRE RESP_RECIBIDO, 
ES_DETA_ESTPR.COD_ETAPA
FROM ES_ESTPREVIO 
INNER JOIN ((ES_ETAPA INNER JOIN ES_D_ETAPA ON (ES_ETAPA.COD_T_CONTRATO = ES_D_ETAPA.COD_T_CONTRATO) AND (ES_ETAPA.COD_ETAPA = ES_D_ETAPA.COD_ETAPA) AND (ES_ETAPA.COMPANIA = ES_D_ETAPA.COMPANIA)) 
INNER JOIN ES_DETA_ESTPR ON (ES_D_ETAPA.COD_T_CONTRATO = ES_DETA_ESTPR.COD_T_CONTRATO) AND (ES_D_ETAPA.COD_ETAPA = ES_DETA_ESTPR.COD_ETAPA) 
AND (ES_D_ETAPA.COD_D_ETAPA = ES_DETA_ESTPR.COD_D_ETAPA) AND (ES_D_ETAPA.COMPANIA = ES_DETA_ESTPR.COMPANIA)
LEFT JOIN RESPONSABLE RES_ENTREGA ON (ES_DETA_ESTPR.COMPANIA = RES_ENTREGA.COMPANIA) AND (ES_DETA_ESTPR.RESP_ENTREGA = RES_ENTREGA.CEDULA)
INNER JOIN TERCERO TER_ENTREGA ON (RES_ENTREGA.COMPANIA=TER_ENTREGA.COMPANIA) AND (RES_ENTREGA.SUCURSAL = TER_ENTREGA.SUCURSAL) AND (RES_ENTREGA.CEDULA = TER_ENTREGA.NIT)) 
ON (ES_ESTPREVIO.COD_ESTUDIO = ES_DETA_ESTPR.COD_ESTUDIO) AND (ES_ESTPREVIO.COMPANIA = ES_DETA_ESTPR.COMPANIA)
LEFT JOIN RESPONSABLE RES_RECIBIDO ON (ES_DETA_ESTPR.COMPANIA = RES_RECIBIDO.COMPANIA) AND (ES_DETA_ESTPR.RESP_RECIBIDO = RES_RECIBIDO.CEDULA)
INNER JOIN TERCERO TER_RECIBIDO ON (RES_RECIBIDO.COMPANIA = TER_RECIBIDO.COMPANIA) AND (RES_RECIBIDO.SUCURSAL = TER_RECIBIDO.SUCURSAL) AND (RES_RECIBIDO.CEDULA= TER_RECIBIDO.NIT)
WHERE ES_ESTPREVIO.COD_ESTUDIO BETWEEN s$estudioIni$s AND s$estudioFin$s
 AND ES_ESTPREVIO.TIPO_DIA = 's$tipoestudio$s' ORDER BY ES_DETA_ESTPR.COD_ETAPA
