SELECT   
        ES_DETA_ESTPR.COD_ESTUDIO,  
        UPPER(ES_ESTPREVIO.NOMBRE_ESTUDIO) AS N_ESTUDIO,  
        ES_DETA_ESTPR.COD_ETAPA,  
        UPPER(ES_ETAPA.NOMBRE) AS N_ETAPA,  
        UPPER(ES_D_ETAPA.DESCRIPCION) AS N_DESCRIPCION, 
        ES_DETA_ESTPR.FECHA_RECIBIDO,  
        UPPER(TERCERO_1.NOMBRE)AS RECIBIDO_POR, 
        DECODE (ES_DETA_ESTPR.REALIZADO ,'-1','X','0',' ') REALIZADO, 
        ES_DETA_ESTPR.OBSERVACIONES, 
        ES_DETA_ESTPR.FECHA_ENTREGA,  
        UPPER(TERCERO.NOMBRE) AS ENTREGADO_POR, 
        DECODE(ES_DETA_ESTPR.VERIFICADO , '-1','X','0',' ') VERIFICADO 
FROM ES_DETA_ESTPR  
  LEFT JOIN ES_ETAPA  
    ON ES_DETA_ESTPR.COMPANIA = ES_ETAPA.COMPANIA  
    AND ES_DETA_ESTPR.COD_ETAPA = ES_ETAPA.COD_ETAPA  
    AND ES_DETA_ESTPR.COD_T_CONTRATO = ES_ETAPA.COD_T_CONTRATO  
  LEFT JOIN ES_D_ETAPA  
    ON ES_DETA_ESTPR.COMPANIA = ES_D_ETAPA.COMPANIA  
    AND ES_DETA_ESTPR.COD_D_ETAPA = ES_D_ETAPA.COD_D_ETAPA  
    AND ES_DETA_ESTPR.COD_ETAPA = ES_D_ETAPA.COD_ETAPA  
    AND ES_DETA_ESTPR.COD_T_CONTRATO = ES_D_ETAPA.COD_T_CONTRATO  
  INNER JOIN TERCERO  
     ON ES_DETA_ESTPR.COMPANIA = TERCERO.COMPANIA 
    AND ES_DETA_ESTPR.RESP_ENTREGA = TERCERO.NIT 
    AND ES_DETA_ESTPR.SUCURSAL_ENTREGA = TERCERO.SUCURSAL 
  INNER JOIN TERCERO TERCERO_1 
     ON ES_DETA_ESTPR.COMPANIA = TERCERO_1.COMPANIA 
    AND ES_DETA_ESTPR.RESP_RECIBIDO = TERCERO_1.NIT 
    AND ES_DETA_ESTPR.SUCURSAL_RECIBIDO = TERCERO_1.SUCURSAL 
   LEFT JOIN ES_ESTPREVIO  
    ON ES_DETA_ESTPR.COMPANIA = ES_ESTPREVIO.COMPANIA  
    AND ES_DETA_ESTPR.COD_ESTUDIO = ES_ESTPREVIO.COD_ESTUDIO   
WHERE ES_DETA_ESTPR.COMPANIA = s$compania$s  
  AND ES_DETA_ESTPR.COD_ESTUDIO = s$codEstudio$s 
  AND ES_DETA_ESTPR.COD_D_ETAPA = s$codEtapa$s  
  AND ES_DETA_ESTPR.COD_ETAPA = s$etapa$s  
  AND ES_DETA_ESTPR.COD_T_CONTRATO = s$codContrato$s
