SELECT TIPOCONTRATO_PR.TIPOCONTRATO, 
       TIPOCONTRATO_PR.DESCRIPCION,
       TRANSACCION.CONSECUTIVO, 
       DECODE (D_TRANSACCION.ESTADO , 'A','Activa' , 'P' , 'Pendiente' , 'C' , 'Cerrada' , 'X' , 'Todas') AS ESTADO, 
       D_TRANSACCION.IDETAPA, 
       ETAPA_PR.DESCRIPCION AS NOMBREETAPA, 
       PROPONENTE.PROPONENTE, 
       TERCERO.NOMBRE, 
       PROPONENTE.FECHAINSCRIPCION, 
       PROPONENTE.DOCUMENTOSOPORTE, 
       DECODE(PROPONENTE.ESTADO,'AC','Aceptado','RE','Rechazado','EV','En Evaluación') AS ESTADOPROPONENTE 
FROM TIPOCONTRATO_PR 
  LEFT JOIN ETAPA_PR 
    INNER JOIN TRANSACCION 
      LEFT JOIN D_TRANSACCION 
        ON TRANSACCION.COMPANIA = D_TRANSACCION.COMPANIA
        AND TRANSACCION.TIPOCONTRATO = D_TRANSACCION.TIPOCONTRATO 
        AND  TRANSACCION.CONSECUTIVO = D_TRANSACCION.TRANSACCION 
      LEFT JOIN PROPONENTE 
        ON  D_TRANSACCION.COMPANIA = PROPONENTE.COMPANIA
        AND D_TRANSACCION.TIPOCONTRATO = PROPONENTE.TIPOCONTRATO 
        AND D_TRANSACCION.TRANSACCION = PROPONENTE.TRANSACCION 
        AND D_TRANSACCION.CONSECUTIVODETALLE = PROPONENTE.CONSECUTIVODETALLE 
      LEFT JOIN TERCERO 
        ON PROPONENTE.COMPANIA = TERCERO.COMPANIA
        AND PROPONENTE.PROPONENTE = TERCERO.NIT 
        AND PROPONENTE.SUCURSAL = TERCERO.SUCURSAL
    ON  ETAPA_PR.COMPANIA = D_TRANSACCION.COMPANIA  
    AND ETAPA_PR.TIPOCONTRATO = D_TRANSACCION.TIPOCONTRATO 
    AND ETAPA_PR.IDETAPA = D_TRANSACCION.IDETAPA 
  ON TIPOCONTRATO_PR.COMPANIA = TRANSACCION.COMPANIA
  AND TIPOCONTRATO_PR.TIPOCONTRATO = TRANSACCION.TIPOCONTRATO  
WHERE TIPOCONTRATO_PR.COMPANIA=s$compania$s  
  AND TIPOCONTRATO_PR.TIPOCONTRATO = 's$tipoContrato$s'
  AND TRANSACCION.CONSECUTIVO BETWEEN 's$contratoInicial$s' AND 's$contratoFinal$s'
  AND ETAPA_PR.IDETAPA BETWEEN 's$etapaInicial$s' AND 's$etapaFinal$s'
  s$estadoTransaccion$s
ORDER BY D_TRANSACCION.ORDEN
