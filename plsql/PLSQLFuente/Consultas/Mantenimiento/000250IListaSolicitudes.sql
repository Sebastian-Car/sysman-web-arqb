SELECT 
   MANTENIMIENTO.NUMERO, 
   MANTENIMIENTO.TIPO, 
   D_MANTENIMIENTO.FECHAINICIAL, 
   D_MANTENIMIENTO.FECHAFINAL, 
   DEPENDENCIA.NOMBRE  DEPENDENCIA, 
   TERCERO.NOMBRE  RESPONSABLE, 
   D_MANTENIMIENTO.CONSECUTIVO, 
   TALLER.NOMBRE  TALLER, 
   MANTENIMIENTO.COMPANIA, 
   MANTENIMIENTO.ANO, 
   MANTENIMIENTO.FECHA, 
   D_MANTENIMIENTO.CODIGOELEMENTO, 
   D_MANTENIMIENTO.NOMBRELEMENTO, 
   D_MANTENIMIENTO.PLACA, 
   D_MANTENIMIENTO.DESCRIPCION, 
   TAREAMANTENI.NOMBRE  TAREAMANTENIMIENTO, 
   D_MANTENIMIENTO.CANTIDAD, 
   D_MANTENIMIENTO.VALORUNITARIO, 
   D_MANTENIMIENTO.VALORTOTAL,
   EXTRACT (DAY FROM MANTENIMIENTO.FECHA) DIA,
   EXTRACT (MONTH FROM MANTENIMIENTO.FECHA) MES,
   EXTRACT (YEAR FROM MANTENIMIENTO.FECHA) ANOM,
   CASE WHEN TIPO_MANTENIMIENTO='P' THEN 'Preventivo' ELSE  CASE WHEN TIPO_MANTENIMIENTO='C' THEN 'Correctivo' ELSE '' END END TIPO_MANTENIMIENTO1,
   DEVOLUTIVO.DESCRIPCION ESPECIFICACION,
   D_MANTENIMIENTO.KILOMETRAJE
FROM 
  MANTENIMIENTO 
    LEFT JOIN D_MANTENIMIENTO 
      ON  MANTENIMIENTO.COMPANIA = D_MANTENIMIENTO.COMPANIA
      AND MANTENIMIENTO.ANO = D_MANTENIMIENTO.ANO
      AND MANTENIMIENTO.TIPO = D_MANTENIMIENTO.TIPO_CPTE
      AND MANTENIMIENTO.NUMERO = D_MANTENIMIENTO.COMPROBANTE        
    LEFT JOIN TAREAMANTENI 
      ON  D_MANTENIMIENTO.COMPANIA = TAREAMANTENI.COMPANIA 
      AND D_MANTENIMIENTO.TAREAMANTENIMIENTO = TAREAMANTENI.CODIGO
    LEFT JOIN TALLER 
      ON  D_MANTENIMIENTO.COMPANIA = TALLER.COMPANIA 
      AND D_MANTENIMIENTO.NIT_TALLER = TALLER.NIT
    INNER JOIN DEPENDENCIA_RESPONSABLE
       ON MANTENIMIENTO.COMPANIA = DEPENDENCIA_RESPONSABLE.COMPANIA
      AND MANTENIMIENTO.DEPENDENCIA = DEPENDENCIA_RESPONSABLE.DEPENDENCIA
      AND MANTENIMIENTO.RESPONSABLE = DEPENDENCIA_RESPONSABLE.RESPONSABLE
      AND MANTENIMIENTO.SUCURSAL = DEPENDENCIA_RESPONSABLE.SUCURSAL
    INNER JOIN DEPENDENCIA
       ON DEPENDENCIA_RESPONSABLE.COMPANIA = DEPENDENCIA.COMPANIA
      AND DEPENDENCIA_RESPONSABLE.DEPENDENCIA = DEPENDENCIA.CODIGO
    INNER JOIN RESPONSABLE
       ON DEPENDENCIA_RESPONSABLE.COMPANIA = RESPONSABLE.COMPANIA
      AND DEPENDENCIA_RESPONSABLE.RESPONSABLE = RESPONSABLE.CEDULA
      AND DEPENDENCIA_RESPONSABLE.SUCURSAL = RESPONSABLE.SUCURSAL
    INNER JOIN TERCERO
       ON RESPONSABLE.COMPANIA = TERCERO.COMPANIA
      AND RESPONSABLE.CEDULA = TERCERO.NIT
      AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
    LEFT JOIN DEVOLUTIVO
      ON D_MANTENIMIENTO.COMPANIA = DEVOLUTIVO.COMPANIA
     AND D_MANTENIMIENTO.CODIGOELEMENTO = DEVOLUTIVO.ELEMENTO
     AND D_MANTENIMIENTO.PLACA = DEVOLUTIVO.SERIE
WHERE MANTENIMIENTO.COMPANIA =s$compania$s 
  AND MANTENIMIENTO.NUMERO = s$numero$s 
  AND MANTENIMIENTO.ANO = s$ano$s
  AND MANTENIMIENTO.TIPO ='s$tipo$s'
ORDER BY D_MANTENIMIENTO.CONSECUTIVO
