SELECT 
  TALLER.NOMBRE, 
  TALLER.NIT, 
  TALLER.DIRECCION, 
  TALLER.TELEFONO, 
  TALLER.FAX, 
  TERCERO.NOMBRE PROPIETARIO 
FROM TALLER INNER JOIN TERCERO
     ON TALLER.COMPANIA=TERCERO.COMPANIA
    AND TALLER.PROPIETARIO=TERCERO.NIT
    AND TALLER.SUCURSAL_PROPIETARIO=TERCERO.SUCURSAL
WHERE 
  TALLER.COMPANIA = s$compania$s
