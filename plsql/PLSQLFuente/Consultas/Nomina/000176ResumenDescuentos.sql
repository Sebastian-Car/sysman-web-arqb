SELECT CONCEPTOS.NOMBRE_CONCEPTO,
       SUM(HISTORICOS.VALOR) AS SUMADEVALOR, 
       HISTORICOS.COMPANIA
  FROM (HISTORICOS LEFT JOIN 
       PERSONAL ON 
       HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO AND
       HISTORICOS.COMPANIA = PERSONAL.COMPANIA) LEFT JOIN 
       CONCEPTOS ON 
       HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO AND 
       HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA
 WHERE 
       HISTORICOS.ANO= s$ano1$s 
   AND HISTORICOS.MES= s$mes1$s 
   AND HISTORICOS.PERIODO= s$periodo1$s 
   AND HISTORICOS.COMPANIA= s$compania$s 
   AND ((HISTORICOS.ID_DE_CONCEPTO>=700
   AND HISTORICOS.ID_DE_CONCEPTO<=798)
   OR
   (HISTORICOS.ID_DE_CONCEPTO>=600 
   AND HISTORICOS.ID_DE_CONCEPTO<=698)) 
   GROUP BY CONCEPTOS.NOMBRE_CONCEPTO, 
   HISTORICOS.COMPANIA
