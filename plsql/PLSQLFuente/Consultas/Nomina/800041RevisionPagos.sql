SELECT  HISTORICOS.COMPANIA, 
        HISTORICOS.ID_DE_PROCESO, 
        TO_CHAR(HISTORICOS.ANO) ANO, 
        HISTORICOS.MES, 
        HISTORICOS.ID_DE_CONCEPTO, 
        CONCEPTOS.NOMBRE_CONCEPTO, 
        SUM(HISTORICOS.VALOR) SUMADEVALOR, 
        CATEGORIA.VLR_INCREMENTO
FROM  ((HISTORICOS 
          LEFT JOIN CONCEPTOS 
                 ON HISTORICOS.COMPANIA              = CONCEPTOS.COMPANIA 
                AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO) 
          LEFT JOIN PERSONAL 
                 ON HISTORICOS.COMPANIA              = PERSONAL.COMPANIA 
                AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO) 
        LEFT JOIN CATEGORIA 
           ON PERSONAL.COMPANIA                = CATEGORIA.COMPANIA 
          AND PERSONAL.ESCALAFON      	     = CATEGORIA.ESCALAFON
          AND PERSONAL.ID_DE_CATEGORIA = CATEGORIA.ID_DE_CATEGORIA
          AND PERSONAL.ANO             	     = CATEGORIA.ANO
WHERE  (HISTORICOS.PERIODO    NOT IN (05)
  AND   CONCEPTOS.CLASE             =  3 
  AND   CATEGORIA.ANO                =  PERSONAL.ANO)
  AND   (HISTORICOS.COMPANIA   = s$compania$s 
  AND   HISTORICOS.ANO               = s$anio$s
  AND   HISTORICOS.MES 
         BETWEEN NVL(PCK_SYSMAN_UTL.FC_PAR(s$compania$s,'DESDE QUE MES REALIZAR RETROACTIVO',PCK_DATOS.FC_MODULONOMINA,SYSDATE),'001') 
         AND NVL(PCK_SYSMAN_UTL.FC_PAR(s$compania$s,'HASTA QUE MES REALIZAR RETROACTIVO',PCK_DATOS.FC_MODULONOMINA,SYSDATE),'001'))
GROUP BY HISTORICOS.COMPANIA, 
      HISTORICOS.ID_DE_PROCESO, 
      HISTORICOS.ANO, 
      HISTORICOS.MES, 
      HISTORICOS.ID_DE_CONCEPTO, 
      CONCEPTOS.NOMBRE_CONCEPTO, 
      CATEGORIA.VLR_INCREMENTO
ORDER BY  HISTORICOS.ID_DE_CONCEPTO
