

SELECT 
  
 UPPER(PAIS_ORIGEN.NOMBRE||' '|| DEP_ORIGEN.NOMBRE||' '||CIUDAD_ORIGEN.NOMBRE) AS CIUDAD,
UPPER(PAIS_DESTINO.NOMBRE||' '|| DEP_DEST.NOMBRE||' '||CIUDAD_DEST.NOMBRE) AS DESTINO,  
  PERSONAL.NUMERO_DCTO,
  SOLICITUDPERSONAL.NUMDIASPER    AS NDPER,
  SOLICITUDPERSONAL.NUMDIASSINPER AS NDSINPER,
  SOLICITUDPERSONAL.VALDIASPER,
  SOLICITUDPERSONAL.VALDIASSINPER,
  SOLICITUDPERSONAL.TOTAL AS TOTVIATICOS,
 TO_CHAR( SOLICITUD.FECHAINICIO,'DD/MM/YYYY') FECHAINICIO, 
  TO_CHAR(   SOLICITUD.FECHAFIN,'DD/MM/YYYY') FECHAFIN ,
  PERSONAL.APELLIDO1,
  PERSONAL.APELLIDO2,
  PERSONAL.NOMBRES,
  SOLICITUD.OBJETO,
  CASE WHEN CV.TIPO_CONCEPTO='T' THEN TOTAL ELSE 0 END  AS TRANSPORTE
FROM VI_DETALLE_VIATICOS  SOLICITUDPERSONAL
INNER JOIN VI_VIATICOS       SOLICITUD
ON SOLICITUDPERSONAL.COMPANIA      = SOLICITUD.COMPANIA
AND SOLICITUDPERSONAL.ANO          = SOLICITUD.ANO
AND SOLICITUDPERSONAL.TIPO_VIATICO = SOLICITUD.TIPO_VIATICO
AND SOLICITUDPERSONAL.NUMERO       = SOLICITUD.CODSOLICITUD
LEFT JOIN PERSONAL
ON SOLICITUDPERSONAL.COMPANIA = PERSONAL.COMPANIA
AND  SOLICITUDPERSONAL.TERCERO = PERSONAL.NUMERO_DCTO
AND SOLICITUDPERSONAL.SUCURSAL = PERSONAL.SUCURSAL
LEFT JOIN VI_CONCEPTO_VIATICOS  CV
ON SOLICITUDPERSONAL.COMPANIA = CV.COMPANIA
AND SOLICITUDPERSONAL.ANO      = CV.ANO
AND SOLICITUDPERSONAL.CONCEPTO  = CV.CODIGO_CONCEPTO
LEFT JOIN PAISES PAIS_ORIGEN
ON SOLICITUD.PAIS_ORIGEN =  PAIS_ORIGEN.PAIS
LEFT JOIN PAISES PAIS_DESTINO
ON SOLICITUD.PAIS =  PAIS_DESTINO.PAIS
LEFT JOIN DEPARTAMENTO DEP_ORIGEN
ON SOLICITUD.PAIS_ORIGEN = DEP_ORIGEN.PAIS
AND SOLICITUD.DEPARTAMENTO_ORIGEN = DEP_ORIGEN.CODIGO
LEFT JOIN DEPARTAMENTO DEP_DEST
ON SOLICITUD.PAIS = DEP_DEST.PAIS
AND SOLICITUD.DEPARTAMENTO = DEP_DEST.CODIGO
LEFT JOIN CIUDAD CIUDAD_ORIGEN
ON SOLICITUD.PAIS_ORIGEN =  CIUDAD_ORIGEN.PAIS
AND SOLICITUD.DEPARTAMENTO_ORIGEN = CIUDAD_ORIGEN.DEPARTAMENTO
AND SOLICITUD.CIUDAD_ORIGEN = CIUDAD_ORIGEN.CODIGO
LEFT JOIN CIUDAD CIUDAD_DEST
ON SOLICITUD.PAIS =  CIUDAD_DEST.PAIS
AND SOLICITUD.DEPARTAMENTO= CIUDAD_DEST.DEPARTAMENTO
AND SOLICITUD.CIUDAD= CIUDAD_DEST.CODIGO
 WHERE SOLICITUDPERSONAL.COMPANIA = s$compania$s
AND PAIS_ORIGEN.PAIS  IN ( s$paisInicial$s)
AND PAIS_DESTINO.PAIS  IN (s$paisFinal$s)
AND DEP_ORIGEN.CODIGO  IN ( s$departamentoInicial$s)
AND DEP_DEST.CODIGO  IN (s$departamentoFinal$s)
AND CIUDAD_ORIGEN.CODIGO IN (s$ciudadInicial$s)
AND CIUDAD_DEST.CODIGO IN (s$ciudadFinal$s)
AND SOLICITUD.FECHAINICIO BETWEEN s$fechaIncial$s AND s$fechaFinal$s
