SELECT  
    HISTORICOS.COMPANIA,  
    HISTORICOS.ID_DE_PROCESO,  
    PERSONAL_HISTORICO.NOMBRE_ESCALAFON  ESCALAFON,  
    CASE WHEN PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA, 31) = '890704536-7' 
    THEN 	CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE = 'A' 
			THEN 'ADMINISTRACION' 
			ELSE 	CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE = 'V' 
					THEN 'POLITICA' 
					ELSE 	CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE = 'P' 
							THEN 'GESTION' 
							ELSE 'EJECUCION' 
							END 
					END 
			END 
    ELSE 	CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE = 'A' 
			THEN  'ADMINISTRACION' 
			ELSE 	CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE = 'V' 
					THEN  'OPERACION/VENTAS' 
					ELSE 	CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE = 'P' 
							THEN  'PRODUCCION/PENSIONADO' 
							ELSE 'OTRO' 
							END 
					END 
			END 
    END  GRUPOCONTABLE,  
    PERSONAL_HISTORICO.FONDO_AFC,  
    PERSONAL_HISTORICO.NOMBRE_FONDOAFC,  
    V_FONDO_DE_PENSION_AFC.NIT, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 127 
        THEN HISTORICOS.VALOR 
        ELSE 0 
        END)  PENSION_VOL_EMPLEADO 
FROM  
    PERSONAL_HISTORICO  
  INNER JOIN HISTORICOS  
    ON (PERSONAL_HISTORICO.COMPANIA = HISTORICOS.COMPANIA) 
   AND (PERSONAL_HISTORICO.ID_DE_PROCESO = HISTORICOS.ID_DE_PROCESO)  
   AND (PERSONAL_HISTORICO.ANO = HISTORICOS.ANO)  
   AND (PERSONAL_HISTORICO.MES = HISTORICOS.MES)  
   AND (PERSONAL_HISTORICO.PERIODO = HISTORICOS.PERIODO)  
   AND (PERSONAL_HISTORICO.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO)        
  INNER JOIN PERIODOS  
    ON (HISTORICOS.COMPANIA = PERIODOS.COMPANIA)  
   AND (HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO)  
   AND (HISTORICOS.ANO = PERIODOS.ANO)  
   AND (HISTORICOS.MES = PERIODOS.MES)   
   AND (HISTORICOS.PERIODO = PERIODOS.PERIODO)         
  INNER JOIN V_FONDO_DE_PENSION_AFC 
    ON (PERSONAL_HISTORICO.COMPANIA = V_FONDO_DE_PENSION_AFC.COMPANIA) 
   AND (PERSONAL_HISTORICO.FONDO_AFC = V_FONDO_DE_PENSION_AFC.FONDO_AFC) 
WHERE  
      PERSONAL_HISTORICO.COMPANIA = s$compania$s 
  AND PERSONAL_HISTORICO.ID_DE_PROCESO = s$idProceso$s 
  AND TRIM(TO_CHAR(PERSONAL_HISTORICO.ANO, '0000')) || TRIM(TO_CHAR(PERSONAL_HISTORICO.MES, '00')) || TRIM(TO_CHAR(PERSONAL_HISTORICO.PERIODO, '00')) BETWEEN s$rangoInicial$s AND s$rangoFinal$s 
  AND HISTORICOS.ID_DE_CONCEPTO IN (127) 
  AND PERIODOS.ACUMULADO NOT IN (0)    
GROUP BY  
    HISTORICOS.COMPANIA,  
    HISTORICOS.ID_DE_PROCESO,  
    PERSONAL_HISTORICO.NOMBRE_ESCALAFON,  
    CASE WHEN PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA, 31) = '890704536-7' 
    THEN 	CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE = 'A' 
			THEN 'ADMINISTRACION' 
			ELSE 	CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE = 'V' 
					THEN 'POLITICA' 
					ELSE 	CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE = 'P' 
							THEN 'GESTION' 
							ELSE 'EJECUCION' 
							END 
					END 
			END 
    ELSE	CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE = 'A' 
			THEN  'ADMINISTRACION' 
			ELSE 	CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE = 'V' 
					THEN  'OPERACION/VENTAS' 
					ELSE 	CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE = 'P' 
							THEN 'PRODUCCION/PENSIONADO' 
							ELSE 'OTRO' 
							END 
					END 
			END 
    END,  
    PERSONAL_HISTORICO.FONDO_AFC,  
    PERSONAL_HISTORICO.NOMBRE_FONDOAFC, 
    V_FONDO_DE_PENSION_AFC.NIT
