SELECT V_FONDO_DE_PENSIONES.NOMBRE_DEL_FONDO,
  V_FONDO_DE_PENSIONES.ID_DEL_FONDO AS ID_DEL_FONDO,
  V_ACUMULADOS.NUMERO_DCTO          AS CEDULA,
  V_ACUMULADOS.NOMBRECOMPLETO       AS NOM_EMPLEADO,
  V_ACUMULADOS.SALARIO_BASE_IBC     AS SALARIO_BASE_IBC,
  V_ACUMULADOS.COMPANIA,
  V_ACUMULADOS.ID_DE_PROCESO                     AS ID_DE_PROCESO,
  V_ACUMULADOS.ANO                               AS ANO,
  PCK_SYSMAN_UTL.FC_NOMBRE_MES(V_ACUMULADOS.MES) AS MES,
  V_ACUMULADOS.COMPANIA                          AS COMPANIAHFA,
  SUM((
  CASE
    WHEN V_ACUMULADOS.ID_DE_CONCEPTO =134
    THEN V_ACUMULADOS.VALOR
    ELSE 0
  END )) AS C134,
  SUM((
  CASE
    WHEN V_ACUMULADOS.ID_DE_CONCEPTO =112
    THEN V_ACUMULADOS.VALOR
    ELSE 0
  END )) AS C112,
  SUM((
  CASE
    WHEN V_ACUMULADOS.ID_DE_CONCEPTO =117
    THEN V_ACUMULADOS.VALOR
    ELSE 0
  END )) AS C117,
  SUM((
  CASE
    WHEN V_ACUMULADOS.ID_DE_CONCEPTO =118
    THEN V_ACUMULADOS.VALOR
    ELSE 0
  END )) AS C118,
  SUM((
  CASE
    WHEN V_ACUMULADOS.ID_DE_CONCEPTO =115
    THEN V_ACUMULADOS.VALOR
    ELSE 0
  END )) AS C115,
  SUM((
  CASE
    WHEN V_ACUMULADOS.ID_DE_CONCEPTO =120
    THEN V_ACUMULADOS.VALOR
    ELSE 0
  END )) AS C120,
  SUM((
  CASE
    WHEN V_ACUMULADOS.ID_DE_CONCEPTO =124
    THEN V_ACUMULADOS.VALOR
    ELSE 0
  END )) AS C124,
  SUM((
  CASE
    WHEN V_ACUMULADOS.ID_DE_CONCEPTO =131
    THEN V_ACUMULADOS.VALOR
    ELSE 0
  END )) AS C131,
  SUM((
  CASE
    WHEN V_ACUMULADOS.ID_DE_CONCEPTO =117
    THEN V_ACUMULADOS.VALOR
    ELSE 0
  END ))+ SUM((
  CASE
    WHEN V_ACUMULADOS.ID_DE_CONCEPTO =118
    THEN V_ACUMULADOS.VALOR
    ELSE 0
  END )) AS C117C118,
  SUM((
  CASE
    WHEN V_ACUMULADOS.ID_DE_CONCEPTO =131
    THEN V_ACUMULADOS.VALOR
    ELSE 0
  END ))+ SUM((
  CASE
    WHEN V_ACUMULADOS.ID_DE_CONCEPTO =118
    THEN V_ACUMULADOS.VALOR
    ELSE 0
  END )) AS C131YC118,
  SUM((
  CASE
    WHEN V_ACUMULADOS.ID_DE_CONCEPTO =117
    THEN V_ACUMULADOS.VALOR
    ELSE 0
  END ))+ SUM((
  CASE
    WHEN V_ACUMULADOS.ID_DE_CONCEPTO =118
    THEN V_ACUMULADOS.VALOR
    ELSE 0
  END ))+ SUM((
  CASE
    WHEN V_ACUMULADOS.ID_DE_CONCEPTO =115
    THEN V_ACUMULADOS.VALOR
    ELSE 0
  END ))+ SUM((
  CASE
    WHEN V_ACUMULADOS.ID_DE_CONCEPTO =120
    THEN V_ACUMULADOS.VALOR
    ELSE 0
  END )) AS TOTAL,
  V_ACUMULADOS.ID_CENTRO_DE_COSTO,
  V_ACUMULADOS.NOMBRE_CENTRO_DE_COSTO,
  V_ACUMULADOS.ID_DE_EMPLEADO,
  V_FONDO_DE_PENSIONES.CODAIFP,
  V_FONDO_DE_PENSIONES.NIT
FROM V_ACUMULADOS
INNER JOIN V_FONDO_DE_PENSIONES
ON V_ACUMULADOS.COMPANIA         = V_FONDO_DE_PENSIONES.COMPANIA
AND V_ACUMULADOS.FONDO_PENSION   = V_FONDO_DE_PENSIONES.ID_DEL_FONDO
WHERE V_ACUMULADOS.COMPANIA      =s$compania$s
AND V_ACUMULADOS.ID_DE_CONCEPTO IN (112,131,132,134,117,118,115,120)
AND V_ACUMULADOS.ID_DE_PROCESO   = s$procesoNomina$s
AND V_ACUMULADOS.ANO             = s$anioNomina$s
AND V_ACUMULADOS.MES             =s$mesNomina$s
AND V_ACUMULADOS.PERIODO         =s$periodoNomina$s
GROUP BY V_FONDO_DE_PENSIONES.NOMBRE_DEL_FONDO,
  V_FONDO_DE_PENSIONES.ID_DEL_FONDO,
  V_ACUMULADOS.NUMERO_DCTO ,
  V_ACUMULADOS.NOMBRECOMPLETO ,
  V_ACUMULADOS.SALARIO_BASE_IBC,
  V_ACUMULADOS.COMPANIA,
  V_ACUMULADOS.ID_DE_PROCESO,
  V_ACUMULADOS.ANO ,
  V_ACUMULADOS.MES,
  V_ACUMULADOS.COMPANIA,
  V_ACUMULADOS.ID_CENTRO_DE_COSTO,
  V_ACUMULADOS.NOMBRE_CENTRO_DE_COSTO,
  V_ACUMULADOS.ID_DE_EMPLEADO,
  V_FONDO_DE_PENSIONES.CODAIFP,
  V_FONDO_DE_PENSIONES.NIT
ORDER BY V_FONDO_DE_PENSIONES.ID_DEL_FONDO,
  V_ACUMULADOS.NUMERO_DCTO
