SELECT V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD,
       V_PERSONAL.NUMERO_DCTO AS CEDULA,
       V_PERSONAL.FONDO_SALUD AS FONDO_SALUD,
       V_PERSONAL.NOMBRES AS NOM_EMPLEADO,
       'Periodo ' || HISTORICOS.PERIODO || ', ' || PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES) || ' de ' || HISTORICOS.ANO AS FECHA,       
       PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES) AS MES,
       HISTORICOS.PERIODO AS PERIODO,
       V_FONDO_DE_SALUD.CODAIFS,
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='134' THEN HISTORICOS.VALOR ELSE 0 END) AS C134,
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='123' THEN HISTORICOS.VALOR ELSE 0 END) AS C123,
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='130' THEN HISTORICOS.VALOR ELSE 0 END) AS C130
FROM HISTORICOS 
INNER JOIN V_PERSONAL 
   ON HISTORICOS.COMPANIA        = V_PERSONAL.COMPANIA
  AND HISTORICOS.ID_DE_EMPLEADO = V_PERSONAL.ID_DE_EMPLEADO
INNER JOIN V_FONDO_DE_SALUD 
   ON V_PERSONAL.COMPANIA     = V_FONDO_DE_SALUD.COMPANIA
  AND V_PERSONAL.FONDO_SALUD = V_FONDO_DE_SALUD.FONDO_SALUD
WHERE 
HISTORICOS.COMPANIA = s$compania$s
AND HISTORICOS.ID_DE_PROCESO= s$procesoNomina$s
AND HISTORICOS.ANO= s$anioNomina$s
AND HISTORICOS.MES= s$mesNomina$s 
AND HISTORICOS.PERIODO = s$periodoNomina$s 
AND HISTORICOS.ID_DE_CONCEPTO IN ('134','130','123')
GROUP BY V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD,
       V_PERSONAL.NUMERO_DCTO, 
       V_PERSONAL.FONDO_SALUD,
       V_PERSONAL.NOMBRES,
       HISTORICOS.ANO,
       HISTORICOS.MES,
       HISTORICOS.PERIODO,
       V_FONDO_DE_SALUD.CODAIFS
