SELECT PERIODOS.ID_DE_PROCESO,  
       PERIODOS.ANO,  
       PERIODOS.MES,  
       PERIODOS.PERIODO,  
       PERIODOS.NOM_PERIODO,  
       CASE PERIODOS.ESTADO WHEN 0 THEN  ' ' WHEN -1  THEN 'x' END ESTADO,  
       CASE PERIODOS.ACUMULADO WHEN 0 THEN  ' ' WHEN  -1  THEN 'x' END ACUMULADO,  
       PERIODOS.FECHAINICIO,  
       PERIODOS.FECHAFINAL,  
       PERIODOS.DIAS,  
       CASE PERIODOS.DIFERIDOS WHEN 0 THEN ' ' WHEN -1  THEN 'x' END DIFERIDOS 
FROM PERIODOS  
WHERE PERIODOS.COMPANIA       = s$compania$s 
  AND PERIODOS.ID_DE_PROCESO  = s$procesoNomina$s 
  AND PERIODOS.ANO NOT IN(0)
