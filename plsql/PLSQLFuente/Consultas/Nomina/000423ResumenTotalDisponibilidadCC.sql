SELECT  
    V_ACUMULADOS.COMPANIA,  
    V_ACUMULADOS.ID_CENTRO_DE_COSTO,  
    V_ACUMULADOS.NOMBRE_CENTRO_DE_COSTO,  
    V_ACUMULADOS.CLASE  CLASE,  
    V_ACUMULADOS.ID_DE_CONCEPTO  ID_DE_CONCEPTO,  
    V_ACUMULADOS.CLASE_NOMBRE_MAYU CLASECONCEPTO, 
    V_ACUMULADOS.NOMBRE_CONCEPTO NOMBRE_CONCEPTO,  
    SUM(V_ACUMULADOS.VALOR)  SUMAVALOR,  
    CASE WHEN PCK_NOMINA_COM1.FC_PARAMETRO(V_ACUMULADOS.COMPANIA, 31) = '899.999.714-1' AND V_ACUMULADOS.ID_CENTRO_DE_COSTO = '02' 
         THEN CONCEPTOS.CTA_CONTRACRE_PPTAL 
         ELSE CONCEPTOS.CTA_CRE_PPTAL 
    END  CTA_CRE_PPTAL,  
    CONCEPTOS.CTA_CONTRACRE_PPTAL  
FROM V_ACUMULADOS  
INNER JOIN CONCEPTOS  
ON V_ACUMULADOS.COMPANIA = CONCEPTOS.COMPANIA 
AND  V_ACUMULADOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO  
WHERE V_ACUMULADOS.COMPANIA = s$compania$s 
AND CONCEPTOS.CLASE IN (3, 8)  
AND V_ACUMULADOS.ID_DE_CONCEPTO NOT IN (490, 491, 492, 493, 494, 495, 496, 497, 498, 499) 
AND LPAD(V_ACUMULADOS.ID_DE_PROCESO, 2, 0) || LPAD(V_ACUMULADOS.ANO, 4, 0) || LPAD(V_ACUMULADOS.MES, 2, 0) || LPAD(V_ACUMULADOS.PERIODO, 2, 0) BETWEEN 's$fechaInicial$s'  AND 's$fechaFinal$s'  
GROUP BY V_ACUMULADOS.COMPANIA,  
    V_ACUMULADOS.ID_CENTRO_DE_COSTO,  
    V_ACUMULADOS.NOMBRE_CENTRO_DE_COSTO,  
    V_ACUMULADOS.CLASE ,  
    V_ACUMULADOS.ID_DE_CONCEPTO ,  
    V_ACUMULADOS.CLASE_NOMBRE_MAYU, 
    V_ACUMULADOS.NOMBRE_CONCEPTO ,   
    CASE WHEN PCK_NOMINA_COM1.FC_PARAMETRO(V_ACUMULADOS.COMPANIA, 31) = '899.999.714-1' AND V_ACUMULADOS.ID_CENTRO_DE_COSTO = '02' 
         THEN CONCEPTOS.CTA_CONTRACRE_PPTAL 
         ELSE CONCEPTOS.CTA_CRE_PPTAL   
    END,   
    CONCEPTOS.CTA_CONTRACRE_PPTAL
