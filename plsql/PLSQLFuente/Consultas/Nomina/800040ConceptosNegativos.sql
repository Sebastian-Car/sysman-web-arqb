SELECT HISTORICOS.COMPANIA, 
       HISTORICOS.ID_DE_PROCESO, 
       HISTORICOS.ANO, 
       HISTORICOS.MES, 
       HISTORICOS.PERIODO, 
       HISTORICOS.ID_DE_EMPLEADO, 
       HISTORICOS.ID_DE_CONCEPTO, 
       HISTORICOS.VALOR
  FROM HISTORICOS
 WHERE HISTORICOS.COMPANIA 	  = s$compania$s
   AND HISTORICOS.ANO 		  = s$anio$s
   AND HISTORICOS.MES  		  = s$mes$s 
   AND HISTORICOS.PERIODO 	  = '05' 
   AND (  (HISTORICOS.ID_DE_CONCEPTO IN (207, 208,209) 
            AND HISTORICOS.VALOR < 0)
       OR (HISTORICOS.VALOR      < 0))
