SELECT PERSONAL.COMPANIA, 
       HISTORICOS.ANO, 
       HISTORICOS.MES, 
       PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES) AS NOMBREMES,
       HISTORICOS.PERIODO,
       HISTORICOS.ID_DE_CONCEPTO,
       SUM(HISTORICOS.VALOR) AS SUMADEVALOR,
       PERSONAL.BANCO,
       BANCO.NOMBREBANCO AS NOMBRE, 
       COUNT(PERSONAL.BANCO) AS NUMEMPL
       FROM HISTORICOS LEFT JOIN 
       (PERSONAL LEFT JOIN 
       BANCO ON 
       PERSONAL.BANCO = BANCO.BANCO AND 
       PERSONAL.COMPANIA = BANCO.COMPANIA) ON 
       HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO AND 
       HISTORICOS.COMPANIA = PERSONAL.COMPANIA
  WHERE
       PERSONAL.Compania= s$compania$s
   AND HISTORICOS.ANO= s$ano1$s 
   AND HISTORICOS.MES= s$mes1$s 
   AND HISTORICOS.PERIODO= s$periodo1$s 
   AND HISTORICOS.ID_DE_CONCEPTO=144
       GROUP BY PERSONAL.COMPANIA, 
       HISTORICOS.ANO, 
       HISTORICOS.MES, 
       HISTORICOS.PERIODO, 
       HISTORICOS.ID_DE_CONCEPTO, 
       PERSONAL.BANCO,
       BANCO.NOMBREBANCO