SELECT PERSONAL.ID_DE_EMPLEADO, 
       PERSONAL.NOMBRECOMPLETO NOMCOMPLETO, 
       INTERRUPCION_VACACIONES.DIASINTERRUPCION  INTERRUMPIDO, 
       INTERRUPCION_VACACIONES.COMPANIA, 
       INTERRUPCION_VACACIONES.FECHA_INICIO, 
       INTERRUPCION_VACACIONES.FECHA_FINAL, 
       INTERRUPCION_VACACIONES.INICIO_DISFRUTE, 
       INTERRUPCION_VACACIONES.FINAL_DISFRUTE, 
       INTERRUPCION_VACACIONES.ID_DE_PROCESO, 
       INTERRUPCION_VACACIONES.FECHAINTERRUPCION
FROM PERSONAL 
LEFT JOIN INTERRUPCION_VACACIONES 
 ON   PERSONAL.COMPANIA       = INTERRUPCION_VACACIONES.COMPANIA 
 AND  PERSONAL.ID_DE_EMPLEADO = INTERRUPCION_VACACIONES.ID_DE_EMPLEADO 
WHERE INTERRUPCION_VACACIONES.COMPANIA        = s$compania$s
  AND INTERRUPCION_VACACIONES.ID_DE_PROCESO   = s$procesoNomina$s
  AND INTERRUPCION_ENDINERO                  NOT IN(0)
