SELECT TO_CHAR(SYSDATE,'DD') AS DIA_ACTUAL,
  UPPER(PCK_SYSMAN_UTL.FC_NOMBRE_MES(EXTRACT (MONTH FROM SYSDATE))) AS MES_ACTUAL,
  TO_CHAR(SYSDATE,'YYYY') AS ANIO_ACTUAL,
  CARGOS.ID_DE_CARGO,
  CARGOS.NOMBRE_DEL_CARGO,
  CARGOS.PLAZAS                               AS PLAZAS,
  COUNT(PERSONAL.ID_DE_CARGO)                 AS ASIGNADAS,
  CARGOS.PLAZAS - COUNT(PERSONAL.ID_DE_CARGO) AS VACANTES
FROM PERSONAL
LEFT JOIN CARGOS
ON PERSONAL.COMPANIA         = CARGOS.COMPANIA
AND PERSONAL.ID_DE_CARGO     = CARGOS.ID_DE_CARGO
WHERE PERSONAL.ESTADO_ACTUAL = 1
AND PERSONAL.COMPANIA        = s$compania$s
AND CARGOS.ESTADOCARGO = 1
GROUP BY PERSONAL.COMPANIA,
  CARGOS.ID_DE_CARGO,
  CARGOS.NOMBRE_DEL_CARGO,
  CARGOS.PLAZAS
ORDER BY CARGOS.ID_DE_CARGO