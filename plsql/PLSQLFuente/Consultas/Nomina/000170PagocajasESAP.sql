SELECT V_CAJA_COMPENSACION.CAJA_COMPENSACION, 
        V_CAJA_COMPENSACION.NIT COMPENSACIONNIT, 
        V_CAJA_COMPENSACION.NOMBRE_CAJA, 
        HISTORICOS.COMPANIA,
        HISTORICOS.ANO, 
        HISTORICOS.MES, 
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 101 THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE,
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 102 THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE_ICBF,
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 103 THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE_SENA,
        PCK_SYSMAN_UTL.FC_ROUND(SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 104 THEN HISTORICOS.VALOR ELSE 0 END),0) AS  APORTE_ESAP,
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 105 THEN HISTORICOS.VALOR ELSE 0 END) AS  APORTE_INSTITUTOS,
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 2 THEN HISTORICOS.VALOR ELSE 0 END) AS ASIGNACIONBASICA, 
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 150 THEN HISTORICOS.VALOR ELSE 0 END) AS BONANUAL, 
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 186 THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMAJUNIO,
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 192 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 175 THEN HISTORICOS.VALOR ELSE 0 END) AS OTROS, 
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 70 THEN HISTORICOS.VALOR ELSE 0 END) AS EXTRAS, 
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 155 THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMAVAC, 
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 186 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 179 THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMATECNICA, 
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 79 THEN HISTORICOS.VALOR ELSE 0 END) AS ALIMENTACION, 
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 61 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 62 THEN HISTORICOS.VALOR ELSE 0 END) AS GASTOSREP, 
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 170 THEN HISTORICOS.VALOR ELSE 0 END) AS INCANTIGUEDAD, 
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 108 THEN HISTORICOS.VALOR ELSE 0 END) AS BASE_PARAFISCAL,
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 80 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 979 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 525 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 533 THEN HISTORICOS.VALOR ELSE 0 END) AS TRANSPORTE, 
        PCK_SYSMAN_UTL.FC_ROUND(SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 108 THEN HISTORICOS.VALOR ELSE 0 END)*9/100,0) AS TOTAL,
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 101 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 102 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 103 THEN HISTORICOS.VALOR ELSE 0 END)+PCK_SYSMAN_UTL.FC_ROUND(SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 104 THEN HISTORICOS.VALOR ELSE 0 END),0)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 105 THEN HISTORICOS.VALOR ELSE 0 END) AS SUMA, 
        CASE WHEN ((PCK_SYSMAN_UTL.FC_ROUND(SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 108 THEN HISTORICOS.VALOR ELSE 0 END)*9/100,0))-(SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 101 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 102 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 103 THEN HISTORICOS.VALOR ELSE 0 END)+PCK_SYSMAN_UTL.FC_ROUND(SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 104 THEN HISTORICOS.VALOR ELSE 0 END),0)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 105 THEN HISTORICOS.VALOR ELSE 0 END))<>0) THEN 'R' ELSE '' END AS DIFERENCIA,
        MAX(HISTORICOS.ID_DE_CONCEPTO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS ID_DE_CONCEPTO, 
        SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 2 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 150 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 186 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 192 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 175 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 70 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 155 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 186 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 179 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 79 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 61 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 62 THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE HISTORICOS.ID_DE_CONCEPTO WHEN 170 THEN HISTORICOS.VALOR ELSE 0 END) AS BASE_PARAFISCAL2, 
        PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES) AS NOMBREMES, 
        PARAMETROS_DE_ENTRADA.NIT AS ENTRADANIT, 
        PARAMETROS_DE_ENTRADA.RAZONSOCIAL, 
        PARAMETROS_DE_ENTRADA.DIRECCION, 
        CIUDAD.NOMBRE AS NOMBRECIUDAD, 
        DEPARTAMENTO.NOMBRE AS NOMBREDEPARTAMENTO, 
        PARAMETROS_DE_ENTRADA.TELEFONO
FROM (((((HISTORICOS LEFT JOIN
        PERSONAL ON 
        HISTORICOS.COMPANIA = PERSONAL.COMPANIA AND 
        HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO) LEFT JOIN 
        V_CAJA_COMPENSACION ON 
        PERSONAL.COMPANIA = V_CAJA_COMPENSACION.COMPANIA AND 
        PERSONAL.CAJA_COMPENSACION = V_CAJA_COMPENSACION.CAJA_COMPENSACION) LEFT JOIN
        CONCEPTOS ON 
        HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO AND 
        HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA) LEFT JOIN
        PARAMETROS_DE_ENTRADA ON 
        PERSONAL.COMPANIA = PARAMETROS_DE_ENTRADA.COMPANIA) LEFT JOIN 
        CIUDAD ON 
        PARAMETROS_DE_ENTRADA.DEPARTAMENTO = CIUDAD.DEPARTAMENTO AND 
        PARAMETROS_DE_ENTRADA.CIUDAD = CIUDAD.CODIGO) LEFT JOIN 
        DEPARTAMENTO ON 
        CIUDAD.PAIS = DEPARTAMENTO.PAIS AND 
        CIUDAD.DEPARTAMENTO = DEPARTAMENTO.CODIGO
        WHERE PERSONAL.ID_DE_TIPO NOT IN (5)
        AND HISTORICOS.COMPANIA= s$compania$s
        AND HISTORICOS.ANO= s$ano1$s 
        AND HISTORICOS.MES= s$mes1$s 
        GROUP BY V_CAJA_COMPENSACION.CAJA_COMPENSACION,
        V_CAJA_COMPENSACION.NIT,
        V_CAJA_COMPENSACION.NOMBRE_CAJA, 
        HISTORICOS.COMPANIA, 
        HISTORICOS.ANO, HISTORICOS.MES, 
        PARAMETROS_DE_ENTRADA.NIT, 
        PARAMETROS_DE_ENTRADA.RAZONSOCIAL, 
        PARAMETROS_DE_ENTRADA.DIRECCION,
        CIUDAD.NOMBRE, 
        DEPARTAMENTO.NOMBRE, 
        PARAMETROS_DE_ENTRADA.TELEFONO
