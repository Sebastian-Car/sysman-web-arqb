SELECT 
        PERSONAL.NOMBRECOMPLETO AS NOMCOMPLETO, 
        PERSONAL.ID_DE_EMPLEADO, 
        PERSONAL.ESTADO_ACTUAL, 
        V_FONDO_DE_RIESGOS.FONDO_RIESGOS AS IDFONDO,
        V_FONDO_DE_RIESGOS.NIT, 
        V_FONDO_DE_RIESGOS.NOMBRE_FONDO_RIESGOS AS NOMBREFONDO, 
        PERSONAL.NUMERO_DCTO,
        PERSONAL.FECHAFONDORIESGOS AS FECHAFONDO
   FROM PERSONAL 
   LEFT JOIN V_FONDO_DE_RIESGOS ON 
   PERSONAL.FONDO_RIESGOS = V_FONDO_DE_RIESGOS.FONDO_RIESGOS AND 
   PERSONAL.COMPANIA = V_FONDO_DE_RIESGOS.COMPANIA
WHERE PERSONAL.ID_DE_EMPLEADO NOT IN (0)
  AND PERSONAL.ESTADO_ACTUAL NOT IN (3)
  AND PERSONAL.COMPANIA = s$compania$s
