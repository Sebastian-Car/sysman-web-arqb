SELECT  TO_CHAR(MAX(PERSONAL_HISTORICO.ESTADO_ACTUAL) KEEP (DENSE_RANK LAST ORDER BY ROWNUM)) ESTADOACTUAL, 
        TO_CHAR(MAX(PERSONAL_HISTORICO.FECHA_DE_INGRESO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM),'DD/MM/YYYY') FECHA_DE_INGRESO, 
        TO_CHAR(MIN(PERSONAL_HISTORICO.FECHA_DE_RETIRO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM),'DD/MM/YYYY') FECHA_DE_RETIRO,  
        PERSONAL_HISTORICO.DCTO_IDENTIDAD, 
        PERSONAL_HISTORICO.NUMERO_DCTO,
        PERSONAL_HISTORICO.APELLIDO1, 
        PERSONAL_HISTORICO.APELLIDO2, 
        MAX(PERSONAL_HISTORICO.NOMBRES) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NOMBRES, 
        PERSONAL_HISTORICO.SALARIO_BASE_IBC SALARIO_BASE_IBC, 
        PERSONAL_HISTORICO.TIPOVINCULACION, 
        TO_CHAR(SUM(CASE WHEN ID_DE_CONCEPTO IN (009, 011) THEN VALOR ELSE 0 END)) DIAS, 
        TO_CHAR(SUM(CASE WHEN ID_DE_CONCEPTO IN (354, 368) THEN VALOR ELSE 0 END)) DIASATEP, 
        PERSONAL_HISTORICO.NING, 
        PERSONAL_HISTORICO.NRET, 
        PERSONAL_HISTORICO.NTDA, 
        PERSONAL_HISTORICO.NTAA, 
        PERSONAL_HISTORICO.NTDAP, 
        PERSONAL_HISTORICO.NTAAP, 
        PERSONAL_HISTORICO.NVSP, 
        PERSONAL_HISTORICO.VTE, 
        PERSONAL_HISTORICO.NVST, 
        PERSONAL_HISTORICO.NSLN, 
        PERSONAL_HISTORICO.NIGE, 
        PERSONAL_HISTORICO.NLMA, 
        PERSONAL_HISTORICO.NVAC, 
        PERSONAL_HISTORICO.NAVP, 
        PERSONAL_HISTORICO.NVCT, 
        PERSONAL_HISTORICO.NIRP, 
        MAX(PERSONAL_HISTORICO.NUM_AUTO_INCAP) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NUM_AUTO_INCAP, 
        MAX(PERSONAL_HISTORICO.VR_INCAP1) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) VR_INCAP, 
        MAX(PERSONAL_HISTORICO.NUM_AUTO_MAT) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NUM_AUTO_MAT, 
        MAX(PERSONAL_HISTORICO.VR_MAT1) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) VR_MAT,
        TO_CHAR(SUM(CASE WHEN ID_DE_CONCEPTO=035 THEN VALOR ELSE 0 END)) DIASVAC, 
        PERSONAL_HISTORICO.NUMEROPATRONAL NUMEROPATRONAL, 
        SUM(CASE WHEN ID_DE_CONCEPTO IN (113, 116, 117, 118, 115, 123) THEN VALOR ELSE 0 END) PAGOS, 
        SUM(CASE WHEN ID_DE_CONCEPTO=112 THEN VALOR ELSE 0 END) BASE, 
        PERSONAL_HISTORICO.ID_DEL_FONDO ID_DEL_FONDO, 
        SUM(CASE WHEN ID_DE_CONCEPTO IN (117, 118) THEN VALOR ELSE 0 END) PENSION, 
        SUM(CASE WHEN ID_DE_CONCEPTO=115 THEN VALOR ELSE 0 END) FSP, 
        SUM(CASE WHEN ID_DE_CONCEPTO=124 THEN VALOR ELSE 0 END) APVOL, 
        SUM(CASE WHEN ID_DE_CONCEPTO=127 THEN VALOR ELSE 0 END) APAFC, 
        SUM(CASE WHEN ID_DE_CONCEPTO=120 THEN VALOR ELSE 0 END) FSPADICIONAL, 
        SUM(CASE WHEN ID_DE_CONCEPTO=207 THEN VALOR ELSE 0 END) BASESALUD, 
        PERSONAL_HISTORICO.FONDO_SALUD FONDO_SALUD, 
        PERSONAL_HISTORICO.NOMBRE_FONDOSALUD, 
        TO_CHAR(SUM(CASE WHEN ID_DE_CONCEPTO=208 THEN VALOR ELSE 0 END)) DIASSALUD, 
        SUM(CASE WHEN ID_DE_CONCEPTO IN (113, 114, 116, 119) THEN VALOR ELSE 0 END) SALUD, 
        SUM(CASE WHEN ID_DE_CONCEPTO=123 THEN VALOR ELSE 0 END) UPC,       
        SUM(CASE WHEN ID_DE_CONCEPTO=212 THEN VALOR ELSE 0 END) BASERIESGOS, 
        TO_CHAR(SUM(CASE WHEN ID_DE_CONCEPTO=209 THEN VALOR ELSE 0 END)) DIASRIESGOS, 
        SUM(CASE WHEN ID_DE_CONCEPTO=111 THEN VALOR ELSE 0 END) RIESGOS,
        SUM(CASE WHEN ID_DE_CONCEPTO=108 THEN VALOR ELSE 0 END) BASEPARAFISCAL, 
        MAX(PERSONAL_HISTORICO.CAJA_COMPENSACION) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) CAJA_COMPENSACION, 
        PERSONAL_HISTORICO.NOMBRE_CAJACOMPENSACION, 
        SUM(CASE WHEN ID_DE_CONCEPTO=101 THEN VALOR ELSE 0 END) CAJAS, 
        SUM(CASE WHEN ID_DE_CONCEPTO=102 THEN VALOR ELSE 0 END) ICBF, 
        SUM(CASE WHEN ID_DE_CONCEPTO=103 THEN VALOR ELSE 0 END) SENA, 
        SUM(CASE WHEN ID_DE_CONCEPTO=104 THEN VALOR ELSE 0 END) ESAP, 
        SUM(CASE WHEN ID_DE_CONCEPTO=105 THEN VALOR ELSE 0 END) INSTITUTOS, 
        PERSONAL_HISTORICO.FONDO_RIESGOS FONDO_RIESGOS, 
        PERSONAL_HISTORICO.SUBTIPOCOTIZANTE SUBTIPOCOTIZANTE ,
        TO_CHAR(SUM(CASE WHEN ID_DE_CONCEPTO IN (356, 357, 359) THEN VALOR ELSE 0 END)) DIASLNR
FROM HISTORICOS
    INNER JOIN PERSONAL_HISTORICO 
        ON HISTORICOS.COMPANIA = PERSONAL_HISTORICO.COMPANIA
      AND HISTORICOS.ID_DE_PROCESO = PERSONAL_HISTORICO.ID_DE_PROCESO
      AND HISTORICOS.ANO = PERSONAL_HISTORICO.ANO
      AND HISTORICOS.MES = PERSONAL_HISTORICO.MES
      AND HISTORICOS.PERIODO = PERSONAL_HISTORICO.PERIODO
      AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL_HISTORICO.ID_DE_EMPLEADO
    INNER JOIN PERIODOS
        ON HISTORICOS.COMPANIA = PERIODOS.COMPANIA
      AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO
      AND HISTORICOS.ANO = PERIODOS.ANO
      AND HISTORICOS.MES = PERIODOS.MES
      AND HISTORICOS.PERIODO = PERIODOS.PERIODO
WHERE HISTORICOS.COMPANIA = s$compania$s
  AND HISTORICOS.ID_DE_PROCESO <> 98
  AND HISTORICOS.ANO = s$anio$s
  AND HISTORICOS.MES = s$mes$s
  AND PERIODOS.ACUMULADO <> 0
GROUP BY PERSONAL_HISTORICO.DCTO_IDENTIDAD, PERSONAL_HISTORICO.NUMERO_DCTO, PERSONAL_HISTORICO.APELLIDO1, PERSONAL_HISTORICO.APELLIDO2, PERSONAL_HISTORICO.TIPOVINCULACION, 
PERSONAL_HISTORICO.SALARIO_BASE_IBC, PERSONAL_HISTORICO.NING, PERSONAL_HISTORICO.NRET, PERSONAL_HISTORICO.NTDA, PERSONAL_HISTORICO.NTAA, 
PERSONAL_HISTORICO.NTDAP, PERSONAL_HISTORICO.NTAAP, PERSONAL_HISTORICO.NVSP, PERSONAL_HISTORICO.VTE, PERSONAL_HISTORICO.NVST, 
PERSONAL_HISTORICO.NSLN, PERSONAL_HISTORICO.NIGE, PERSONAL_HISTORICO.NLMA, PERSONAL_HISTORICO.NVAC, PERSONAL_HISTORICO.NAVP, 
PERSONAL_HISTORICO.NVCT, PERSONAL_HISTORICO.NIRP, PERSONAL_HISTORICO.NOMBRE_CAJACOMPENSACION, PERSONAL_HISTORICO.NOMBRE_FONDOSALUD, PERSONAL_HISTORICO.NOMBRE_FONDOPENSION, 
PERSONAL_HISTORICO.FONDO_SALUD, PERSONAL_HISTORICO.ID_DEL_FONDO, PERSONAL_HISTORICO.FONDO_RIESGOS, PERSONAL_HISTORICO.NUMEROPATRONAL, PERSONAL_HISTORICO.SUBTIPOCOTIZANTE
ORDER BY MAX(HISTORICOS.ID_DE_EMPLEADO), PERSONAL_HISTORICO.APELLIDO1, PERSONAL_HISTORICO.APELLIDO2, MAX(PERSONAL_HISTORICO.NOMBRES)
