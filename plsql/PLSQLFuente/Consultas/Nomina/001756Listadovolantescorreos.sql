  SELECT 
  'VOLANTE DE PAGO' DOCUMENTOENVIADO,
  CASE WHEN PERSONAL_HISTORICO.ENVIO_CORREO > 0 THEN 'SI' ELSE 'NO' END ENVIADO,
  PERSONAL_HISTORICO.NOMBRECOMPLETO,
  PERSONAL_HISTORICO.ID_DE_EMPLEADO,
  PERSONAL_HISTORICO.NUMERO_DCTO,
  PERSONAL_HISTORICO.COMPANIA,
  PERSONAL_HISTORICO.ANO,
  PERSONAL_HISTORICO.MES,
  PERSONAL_HISTORICO.PERIODO,
  PERSONAL.EMAIL_CORPORATIVO
FROM PERSONAL_HISTORICO
INNER  JOIN PERSONAL
ON PERSONAL.COMPANIA = PERSONAL_HISTORICO.COMPANIA
AND PERSONAL.ID_DE_EMPLEADO = PERSONAL_HISTORICO.ID_DE_EMPLEADO
WHERE PERSONAL_HISTORICO.COMPANIA    = s$compania$s
AND PERSONAL_HISTORICO.ID_DE_PROCESO = s$proceso$s
AND PERSONAL_HISTORICO.ANO           = s$ano$s
AND PERSONAL_HISTORICO.MES           = s$mes$s
AND PERSONAL_HISTORICO.PERIODO       = s$periodo$s
