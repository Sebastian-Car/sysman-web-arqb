SELECT  HISTORICOS.COMPANIA, 
    HISTORICOS.ID_DE_PROCESO, 
    HISTORICOS.ANO, 
    HISTORICOS.MES, 
    HISTORICOS.PERIODO, 
    HISTORICOS.ID_DE_EMPLEADO, 
    HISTORICOS.ID_DE_CONCEPTO, 
    HISTORICOS.FECHA, 
    HISTORICOS.VALOR, 
    HISTORICOS.OBSERVACIONES, 
    PERSONAL.APELLIDO1, 
    PERSONAL.APELLIDO2, 
    PERSONAL.NOMBRES, 
    PERSONAL.FONDO_CESANTIAS, 
    PERSONAL.ESTADO_ACTUAL
FROM  HISTORICOS 
  LEFT JOIN PERSONAL 
     ON  HISTORICOS.ID_DE_EMPLEADO  = PERSONAL.ID_DE_EMPLEADO 
    AND HISTORICOS.COMPANIA              = PERSONAL.COMPANIA
WHERE HISTORICOS.COMPANIA       	= s$compania$s 
  AND HISTORICOS.ANO            	= s$anioNomina$s 
  AND HISTORICOS.MES            	= s$mesNomina$s
  AND HISTORICOS.PERIODO        	=  05 
  AND HISTORICOS.ID_DE_CONCEPTO IN (277,483) 
  AND HISTORICOS.VALOR          	NOT IN (0) 
  AND PERSONAL.FONDO_CESANTIAS  =  15 
  AND PERSONAL.ESTADO_ACTUAL    	=  3
