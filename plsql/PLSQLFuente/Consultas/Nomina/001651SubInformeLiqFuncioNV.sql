SELECT TERCERO.APELLIDO1
  || ' '
  || TERCERO.APELLIDO2
  || ' '
  || TERCERO.NOMBRE FUNCIONARIO,
  VI_DETALLE_VIATICOS.NUMDIASPER,
  VI_DETALLE_VIATICOS.NUMDIASSINPER,
  VI_DETALLE_VIATICOS.VALDIASPER,
  VI_DETALLE_VIATICOS.VALDIASSINPER,
  VI_DETALLE_VIATICOS.TOTAL,
  VI_DETALLE_VIATICOS.NUMERO CODSOLICITUD,
  CASE VI_CONCEPTO_VIATICOS.TIPO_CONCEPTO
    WHEN 'T'
    THEN VI_DETALLE_VIATICOS.TOTAL
    ELSE 0
  END TRANSPORTE,
  CASE VI_CONCEPTO_VIATICOS.TIPO_CONCEPTO
    WHEN 'H'
    THEN VI_DETALLE_VIATICOS.TOTAL
    ELSE 0
  END VIATICOS
FROM TERCERO
  INNER JOIN VI_DETALLE_VIATICOS
    ON TERCERO.COMPANIA= VI_DETALLE_VIATICOS.COMPANIA
    AND TERCERO.NIT    = VI_DETALLE_VIATICOS.TERCERO
  INNER JOIN VI_CONCEPTO_VIATICOS
    ON VI_DETALLE_VIATICOS.COMPANIA  = VI_CONCEPTO_VIATICOS.COMPANIA
    AND VI_DETALLE_VIATICOS.ANO      = VI_CONCEPTO_VIATICOS.ANO
    AND VI_DETALLE_VIATICOS.CONCEPTO = VI_CONCEPTO_VIATICOS.CODIGO_CONCEPTO
WHERE TERCERO.COMPANIA           = $P{PR_COMPANIA}
  AND VI_DETALLE_VIATICOS.NUMERO = $P{PR_CODSOLICITUD}
