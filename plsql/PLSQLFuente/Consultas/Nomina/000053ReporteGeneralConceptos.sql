SELECT SUM(HISTORICOS.VALOR)  TOTAL,
       CONCEPTOS.ID_DE_CONCEPTO, 
       CONCEPTOS.NOMBRE_CONCEPTO, 
       COUNT(*)  CUENTA, 
       AVG(HISTORICOS.VALOR)  PROMEDIO, 
       MAX(HISTORICOS.VALOR)  MAXIMO, 
       MIN(HISTORICOS.VALOR)  MINIMO, 
       PERIODOS.FECHAFINAL, 
       CASE CONCEPTOS.CLASE WHEN 3 THEN 'Remuneración' WHEN 5 THEN 'Descuento' END CLASE 
FROM HISTORICOS INNER JOIN PERIODOS 
  ON HISTORICOS.COMPANIA      = PERIODOS.COMPANIA
 AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO 
 AND HISTORICOS.ANO           = PERIODOS.ANO 
 AND HISTORICOS.MES           = PERIODOS.MES 
 AND HISTORICOS.PERIODO       = PERIODOS.PERIODO 
INNER JOIN CONCEPTOS 
  ON HISTORICOS.COMPANIA        = CONCEPTOS.COMPANIA
 AND HISTORICOS.ID_DE_CONCEPTO  = CONCEPTOS.ID_DE_CONCEPTO 
WHERE HISTORICOS.COMPANIA       =  s$compania$s 
  AND HISTORICOS.ID_DE_PROCESO  =  s$procesoNomina$s
  AND HISTORICOS.ANO            =  s$anioNomina$s
  AND HISTORICOS.MES            =  s$mesNomina$s
  AND HISTORICOS.PERIODO        =  s$periodoNomina$s
  AND CONCEPTOS.CLASE           IN (3, 5) 
GROUP BY CONCEPTOS.ID_DE_CONCEPTO, 
         CONCEPTOS.NOMBRE_CONCEPTO, 
         CONCEPTOS.CLASE,
         PERIODOS.FECHAFINAL
