SELECT CASE WHEN DOCUMENTOS.SIGLA2 IS NULL THEN 'C' ELSE DOCUMENTOS.SIGLA2 END                                    AS TIPO_DOC,
  PERSONAL.NUMERO_DCTO                                                                                            AS NO_DOC,
  PERSONAL.NOMBRES                                                                                                AS PRIMER_NOMBRE,
  ''                                                                                                              AS SEGUNDO_NOMBRE,
  PERSONAL.APELLIDO1                                                                                              AS PRIMER_APELLIDO,
  PERSONAL.APELLIDO2                                                                                              AS SEGUNDO_APELLIDO,
  FONDO_DE_CESANTIAS.CODAIFC                                                                                      AS COD_ADMCESANTIAS,
  PARAMETROS_DE_ENTRADA.DEPARTAMENTO                                                                              AS COD_DPTO,
  PARAMETROS_DE_ENTRADA.CIUDAD                                                                                    AS COD_MUNICIPIO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='913' THEN VALOR ELSE 0 END)                                                         AS SALARIO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='277' THEN VALOR ELSE 0 END )+SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO='483' THEN VALOR ELSE 0 END) AS VALOR_CESANTIAS,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='910' THEN VALOR ELSE 0 END )                                                         AS DIAS_TRABAJADOS,
  PERSONAL.TELEFONOS                                                                                              AS TELEFONO,
  PERSONAL.DIRECCION                                                                                              AS DIRECCION,
  PERSONAL.EMAIL_PERSONAL                                                                                         AS CORREO,
  TO_CHAR(PERSONAL.FECHA_DE_INGRESO,'DD/MM/YYYY') FECHA_DE_INGRESO,
  FONDO_DE_CESANTIAS.NIT,
  FONDO_DE_CESANTIAS.NOMBRE_FONDO_CESANTIAS,
  PERSONAL.NOMBRECOMPLETO AS NOMCOMPLETO
FROM HISTORICOS
INNER JOIN PERSONAL
ON HISTORICOS.COMPANIA        = PERSONAL.COMPANIA
AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO

LEFT JOIN V_FONDO_DE_CESANTIAS FONDO_DE_CESANTIAS
ON PERSONAL.COMPANIA          = FONDO_DE_CESANTIAS.COMPANIA
AND PERSONAL.FONDO_CESANTIAS  = FONDO_DE_CESANTIAS.FONDO_CESANTIAS

LEFT JOIN TIPOS_DOCUMENTOS DOCUMENTOS
ON PERSONAL.COMPANIA      = DOCUMENTOS.COMPANIA
AND PERSONAL.DCTO_IDENTIDAD = DOCUMENTOS.DCTO_IDENTIDAD

LEFT JOIN PARAMETROS_DE_ENTRADA
ON PERSONAL.COMPANIA               = PARAMETROS_DE_ENTRADA.COMPANIA
WHERE  HISTORICOS.COMPANIA         = s$compania$s
AND (PERSONAL.REGIMEN=1 OR PERSONAL.REGIMEN     =2)
AND HISTORICOS.ANO              = s$ano$s
AND HISTORICOS.MES              = s$mes$s
s$fondo$s
AND (HISTORICOS.ID_DE_CONCEPTO   ='277'
OR HISTORICOS.ID_DE_CONCEPTO     ='910'
OR HISTORICOS.ID_DE_CONCEPTO     ='911'
OR HISTORICOS.ID_DE_CONCEPTO     ='912'
OR HISTORICOS.ID_DE_CONCEPTO     ='913'
OR HISTORICOS.ID_DE_CONCEPTO     ='269'
OR HISTORICOS.ID_DE_CONCEPTO     ='483'
)
GROUP BY CASE WHEN DOCUMENTOS.SIGLA2 IS NULL THEN 'C' ELSE DOCUMENTOS.SIGLA2 END,
  PERSONAL.NUMERO_DCTO,
  PERSONAL.NOMBRES,
  PERSONAL.APELLIDO1,
  PERSONAL.APELLIDO2,
  FONDO_DE_CESANTIAS.CODAIFC,
  PARAMETROS_DE_ENTRADA.DEPARTAMENTO,
  PARAMETROS_DE_ENTRADA.CIUDAD,
  PERSONAL.TELEFONOS,
  PERSONAL.DIRECCION,
  PERSONAL.EMAIL_PERSONAL,
  PERSONAL.FECHA_DE_INGRESO,
  FONDO_DE_CESANTIAS.NIT,
  FONDO_DE_CESANTIAS.NOMBRE_FONDO_CESANTIAS,
  PERSONAL.NOMBRECOMPLETO 
