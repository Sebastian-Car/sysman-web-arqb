SELECT HISTORICOS.COMPANIA,
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('370') 
           THEN HISTORICOS.VALOR 
           ELSE 0 
           END)  C370, 
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('371')
           THEN HISTORICOS.VALOR 
           ELSE 0 
           END)  C371, 
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('372') 
           THEN HISTORICOS.VALOR
           ELSE 0 
           END)  C372, 
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('373') 
           THEN HISTORICOS.VALOR 
           ELSE 0 
           END)  C373, 
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('374') 
           THEN HISTORICOS.VALOR 
           ELSE 0 
           END)  C374, 
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('375')
           THEN HISTORICOS.VALOR
           ELSE 0 
           END)  C375, 
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('376')
           THEN HISTORICOS.VALOR 
           ELSE 0 
           END)  C376, 
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('377') 
           THEN HISTORICOS.VALOR 
           ELSE 0 
           END)  C377, 
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('378') 
           THEN HISTORICOS.VALOR 
           ELSE 0 
           END)  C378, 
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('379') 
           THEN HISTORICOS.VALOR 
           ELSE 0 
           END)  C379, 
      SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('001') 
          THEN HISTORICOS.VALOR 
          ELSE 0 
          END)  C001, 
      V_FONDO_DE_SALUD.NIT, 
      PERSONAL_HISTORICO.NOMBRE_FONDOSALUD NOMBRE_FONDO_SALUD, 
      PERSONAL_HISTORICO.NOMBRE_TIPO, 
      SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('130')
          THEN HISTORICOS.VALOR
          ELSE 0 
          END) DTOSALUDEMPLEADO, 
      PERSONAL_HISTORICO.NOMBRECOMPLETO  NOMBRECOMPLETO,
      PERSONAL_HISTORICO.ID_DE_EMPLEADO,
      PERSONAL_HISTORICO.FONDO_SALUD,
      PERSONAL_HISTORICO.SALARIO_BASE_IBC SALARIO_BASE_IBC
 FROM HISTORICOS 
  LEFT JOIN PERSONAL_HISTORICO 
  ON HISTORICOS.COMPANIA = PERSONAL_HISTORICO.COMPANIA 
  AND HISTORICOS.ID_DE_PROCESO = PERSONAL_HISTORICO.ID_DE_PROCESO
  AND HISTORICOS.ANO = PERSONAL_HISTORICO.ANO
  AND HISTORICOS.MES = PERSONAL_HISTORICO.MES
  AND HISTORICOS.PERIODO = PERSONAL_HISTORICO.PERIODO
  AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL_HISTORICO.ID_DE_EMPLEADO   
LEFT JOIN V_FONDO_DE_SALUD 
  ON PERSONAL_HISTORICO.COMPANIA = V_FONDO_DE_SALUD.COMPANIA 
  AND PERSONAL_HISTORICO.FONDO_SALUD = V_FONDO_DE_SALUD.FONDO_SALUD 
WHERE HISTORICOS.COMPANIA= s$compania$s
AND HISTORICOS.ID_DE_PROCESO= s$proceso$s 
AND HISTORICOS.ANO BETWEEN s$ano1$s AND s$ano2$s
AND HISTORICOS.MES BETWEEN s$mes1$s AND s$mes2$s
AND HISTORICOS.PERIODO BETWEEN s$periodo1$s AND s$periodo2$s
AND HISTORICOS.ID_DE_CONCEPTO IN ('370' , '371' , '372' , '373' , '374', '375', '376', '377', '378', '379' , '348', '001') 
AND V_FONDO_DE_SALUD.NIT IS NOT NULL  
GROUP BY HISTORICOS.COMPANIA, 
V_FONDO_DE_SALUD.NIT, 
V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD, 
PERSONAL_HISTORICO.NOMBRE_TIPO, 
PERSONAL_HISTORICO.NOMBRECOMPLETO, 
PERSONAL_HISTORICO.ID_DE_EMPLEADO, 
V_FONDO_DE_SALUD.FONDO_SALUD, 
PERSONAL_HISTORICO.SALARIO_BASE_IBC,  
HISTORICOS.ANO, 
HISTORICOS.MES, 
PERSONAL_HISTORICO.NOMBRE_FONDOSALUD, 
PERSONAL_HISTORICO.FONDO_SALUD
 HAVING (SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('379')
             THEN HISTORICOS.VALOR
             ELSE 0
             END)<>0)
