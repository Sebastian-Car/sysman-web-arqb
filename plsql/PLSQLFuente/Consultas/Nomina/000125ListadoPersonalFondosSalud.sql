SELECT DISTINCT
        PERSONAL.NOMBRECOMPLETO NOMCOMPLETO, 
        PERSONAL.ID_DE_EMPLEADO,
        PERSONAL.ESTADO_ACTUAL,
        V_FONDO_DE_SALUD.CODAIFS, 
        V_FONDO_DE_SALUD.FONDO_SALUD, 
        V_FONDO_DE_SALUD.NIT,
        V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD, 
        PERSONAL.NUMERO_DCTO, 
        PERSONAL.FECHAFONDOSALUD
   FROM PERSONAL 
   LEFT JOIN V_FONDO_DE_SALUD ON 
   PERSONAL.COMPANIA = V_FONDO_DE_SALUD.COMPANIA AND 
   PERSONAL.FONDO_SALUD = V_FONDO_DE_SALUD.FONDO_SALUD
   WHERE PERSONAL.ID_DE_EMPLEADO NOT IN (0)
     AND PERSONAL.ESTADO_ACTUAL NOT IN (3)
     AND PERSONAL.COMPANIA= s$compania$s
