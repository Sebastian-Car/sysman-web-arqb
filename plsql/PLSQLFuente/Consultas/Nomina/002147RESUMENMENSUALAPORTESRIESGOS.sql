SELECT 
	HISTORICOS.ID_DE_PROCESO,
	HISTORICOS.ANO,
	TO_CHAR(HISTORICOS.FECHA, 'MONTH','nls_date_language=spanish') AS MES,
	HISTORICOS.PERIODO,
	PERSONAL.ID_CENTRO_DE_COSTO,
	CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO, 
	PERSONAL.ESTADO_ACTUAL,
	DEPENDENCIA.CODIGO AS DEPENDENCIA,
	DEPENDENCIA.NOMBRE,
	V_FONDO_DE_RIESGOS.NOMBRE_FONDO_RIESGOS,
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '2' THEN HISTORICOS.VALOR ELSE 0 END) AS SUMADESUELDO,
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '112' THEN HISTORICOS.VALOR ELSE 0 END) AS SUMADEIBC,
	SUM( CASE WHEN ID_DE_CONCEPTO = '212' THEN VALOR ELSE 0 END) AS SUMADEIBC_POR_DLABORADOS,
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '111' THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE_RIESGOS,
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '147' THEN HISTORICOS.VALOR ELSE 0 END) AS SUMADEDESC_INCAPACIDAD,
	SUM( CASE WHEN ID_DE_CONCEPTO = '111' THEN VALOR ELSE 0 END) AS TOTAL_RIESGOS,
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '142' THEN HISTORICOS.VALOR ELSE 0 END) AS AJUSTE,
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '111' THEN HISTORICOS.VALOR ELSE 0 END) AS TOTALRIESGOS,
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '111' THEN HISTORICOS.VALOR ELSE 0 END) AS RIESGOTOTAL
FROM 
	HISTORICOS
	INNER JOIN PERSONAL
		ON HISTORICOS.COMPANIA = PERSONAL.COMPANIA
		AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
	INNER JOIN DEPENDENCIA
		ON PERSONAL.COMPANIA = DEPENDENCIA.COMPANIA
		AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
	INNER JOIN CENTRO_COSTO
		ON PERSONAL.ANO = CENTRO_COSTO.ANO
		AND PERSONAL.COMPANIA = CENTRO_COSTO.COMPANIA
		AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
	INNER JOIN V_FONDO_DE_RIESGOS
		ON PERSONAL.COMPANIA = V_FONDO_DE_RIESGOS.COMPANIA
		AND PERSONAL.FONDO_RIESGOS = V_FONDO_DE_RIESGOS.FONDO_RIESGOS
WHERE
	HISTORICOS.ID_DE_PROCESO = s$proceso$s
	AND HISTORICOS.ANO = s$ano$s
	AND HISTORICOS.MES = s$mes$s
	AND HISTORICOS.PERIODO = s$periodo$s
	AND PERSONAL.ESTADO_ACTUAL BETWEEN 1 AND 3
GROUP BY 
	HISTORICOS.ID_DE_PROCESO,
	HISTORICOS.ANO,
	HISTORICOS.FECHA,
	HISTORICOS.PERIODO,
	PERSONAL.ID_CENTRO_DE_COSTO,
	CENTRO_COSTO.NOMBRE, 
	PERSONAL.ESTADO_ACTUAL,
	DEPENDENCIA.CODIGO,
	DEPENDENCIA.NOMBRE,
	V_FONDO_DE_RIESGOS.NOMBRE_FONDO_RIESGOS