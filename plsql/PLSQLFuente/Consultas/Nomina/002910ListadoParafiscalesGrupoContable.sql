MERGE INTO CONSULTAS FIN USING (SELECT '002910ListadoParafiscalesGrupoContable' INFORME ,TO_CLOB(q'[SELECT  HISTORICOS.ID_DE_PROCESO, 
        HISTORICOS.ANO, 
        HISTORICOS.MES, 
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '108' THEN VALOR ELSE 0 END)  AS FACTOR, 
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '101' THEN VALOR ELSE 0 END ) AS COMPENSACION, 
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '102' THEN VALOR ELSE 0 END ) AS BIENESTAR, 
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '103' THEN VALOR ELSE 0 END ) AS SENA, 
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '104' THEN VALOR ELSE 0 END ) AS ESAP, 
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '105' THEN VALOR ELSE 0 END ) AS INSTITUTOS, 
        PERSONAL_HISTORICO.GRUPOCONTABLE, 
        GRUPO_CONTABLE.NOMBRE NOMBRE_GRUPOCONTABLE,
        ROUND(SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '101' THEN VALOR ELSE 0 END ) +
              SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '102' THEN VALOR ELSE 0 END ) +
              SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '103' THEN VALOR ELSE 0 END ) +
              SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '105' THEN VALOR ELSE 0 END ) +
              SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '104' THEN VALOR ELSE 0 END ) ,0) AS REDONDEAR
FROM HISTORICOS 
INNER JOIN PERSONAL_HISTORICO
  ON HISTORICOS.COMPANIA       = PERSONAL_HISTORICO.COMPANIA 
  AND HISTORICOS.ID_DE_PROCESO  = PERSONAL_HISTORICO.ID_DE_PROCESO
  AND HISTORICOS.ANO            = PERSONAL_HISTORICO.ANO 
  AND HISTORICOS.MES            = PERSONAL_HISTORICO.MES 
  AND HISTORICOS.PERIODO        = PERSONAL_HISTORICO.PERIODO 
  AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL_HISTORICO.ID_DE_EMPLEADO 
LEFT JOIN GRUPO_CONTABLE 
  ON GRUPO_CONTABLE.CODIGO = PERSONAL_HISTORICO.GRUPOCONTABLE
WHERE HISTORICOS.COMPANIA      =  s$compania$s
  AND HISTORICOS.ID_DE_CONCEPTO IN (101, 102, 103, 104, 105, 108)
  AND HISTORICOS.ID_DE_PROCESO =  s$proceso$s
  AND HISTORICOS.ANO           =  s$anio$s
  AND HISTORICOS.MES           =  s$mes$s
  s$filtro$s
GROUP BY HISTORICOS.ID_DE_PROCESO,  
         HISTORICOS.ANO, 
         HISTORICOS.MES, 
         PERSONAL_HISTORICO.GRUPOCONTABLE, 
         GRUPO_CONTABLE.NOMBRE]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, 'MPEREZ' CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
