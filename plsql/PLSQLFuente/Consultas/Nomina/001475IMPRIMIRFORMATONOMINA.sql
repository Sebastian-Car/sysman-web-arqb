SELECT PERIODOS.COMPANIA,
  PERIODOS.ID_DE_PROCESO,
  PERIODOS.ANO,
  PERIODOS.MES,
  PERIODOS.PERIODO,
  PERIODOS.NOM_PERIODO,
  PERIODOS.ESTADO,
  PERIODOS.ACUMULADO,
  PERIODOS.FECHAINICIO,
  PERIODOS.FECHAFINAL,
  PERIODOS.DIAS,
  PERIODOS.DIFERIDOS,
  PERIODOS.HIST_FONDOS,
  REPLACE('s$nombreEmpresa$s',' - ',' ')AS NOMPER, 
  TO_CHAR(PERIODOS.FECHAINICIO,'MONTH') AS NOMBREMES
FROM PERIODOS
WHERE PERIODOS.COMPANIA  = 's$compania$s'
AND PERIODOS.ID_DE_PROCESO=s$iddeproceso$s
AND PERIODOS.ANO          =s$anos$s
AND PERIODOS.MES          =s$mes$s
AND PERIODOS.PERIODO      =s$periodo$s
