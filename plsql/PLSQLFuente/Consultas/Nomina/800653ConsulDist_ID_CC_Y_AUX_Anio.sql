MERGE INTO CONSULTAS FIN USING (SELECT '800653ConsulDist_ID_CC_Y_AUX_Anio' INFORME ,TO_CLOB(q'[SELECT DISTRIBUCION.COMPANIA, 
DISTRIBUCION.ID_DE_PROCESO, 
DISTRIBUCION.ANO, 
DISTRIBUCION.ID_CENTRO_DE_COSTO, 
CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO, 
DISTRIBUCION.ID_AUXILIAR, 
AUXILIAR.NOMBRE, 
DISTRIBUCION.ID_DE_CONCEPTO, 
DISTRIBUCION.VALOR
FROM DISTRIBUCION 
INNER JOIN PERSONAL 
ON DISTRIBUCION.COMPANIA = PERSONAL.COMPANIA 
AND DISTRIBUCION.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO 
LEFT JOIN CENTRO_COSTO 
ON PERSONAL.COMPANIA = CENTRO_COSTO.COMPANIA 
AND PERSONAL.ANO = CENTRO_COSTO.ANO 
AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO 
INNER JOIN AUXILIAR ON DISTRIBUCION.COMPANIA = AUXILIAR.COMPANIA 
AND DISTRIBUCION.ID_AUXILIAR = AUXILIAR.CODIGO 
INNER JOIN CONCEPTOS 
ON DISTRIBUCION.COMPANIA = CONCEPTOS.COMPANIA 
AND DISTRIBUCION.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
WHERE DISTRIBUCION.COMPANIA = s$compania$s
AND DISTRIBUCION.ANO = s$anio$s]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);