WITH SUMACUMULADOS AS
  (SELECT COMPANIA,
    ID_DE_EMPLEADO,
    SUM(DEVENGOS) DEVENGOS,
    SUM(DESCUENTOS) DESCUENTOS,
    SUM(NETOAPAGAR) NETO
  FROM V_ACUMULADOS
  WHERE V_ACUMULADOS.COMPANIA    = s$compania$s
  AND V_ACUMULADOS.ID_DE_PROCESO = s$proceso$s
  AND V_ACUMULADOS.ANO           = s$ano$s
  AND V_ACUMULADOS.MES           = s$mes$s
  AND V_ACUMULADOS.PERIODO       = s$periodo$s
  GROUP BY ID_DE_EMPLEADO,
    COMPANIA
  )
SELECT SUMACUMULADOS.DEVENGOS,
  SUMACUMULADOS.DESCUENTOS,
  SUMACUMULADOS.NETO,
  SUB.*
FROM
  (SELECT *
  FROM
    (SELECT V_ACUMULADOS.COMPANIA,
      V_ACUMULADOS.ID_DE_EMPLEADO,
      ESTABLECIMIENTOS_DOCENTES.NOMBRE UBICACION,
      FUENTE_RECURSOS.NOMBRE FUENTE,
      REFERENCIA.NOMBRE PROYECTO,
      V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD "FONDO SALUD",
      V_FONDO_DE_PENSIONES.NOMBRE_DEL_FONDO "FONDO PESNSION",
      PERSONAL.NUMERO_DCTO CEDULA,
      PERSONAL.NOMBRECOMPLETO NOMBRE_EMPLEADO,
      V_ACUMULADOS.CUENTA,
      V_ACUMULADOS.VALOR VALOR,
      CONCEPTOS.CLASE
      ||'_'
      ||LPAD(CONCEPTOS.ID_DE_CONCEPTO,3,0)
      ||'_'
      || CONCEPTOS.NOMBRE_CONCEPTO NOMBRECONCEPTO
    FROM V_ACUMULADOS
    INNER JOIN PERSONAL
    ON V_ACUMULADOS.COMPANIA        = PERSONAL.COMPANIA
    AND V_ACUMULADOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
    INNER JOIN CONCEPTOS
    ON V_ACUMULADOS.COMPANIA                = CONCEPTOS.COMPANIA
    AND V_ACUMULADOS.ID_DE_CONCEPTO         = CONCEPTOS.ID_DE_CONCEPTO
    INNER JOIN ESTABLECIMIENTOS_DOCENTES
    ON ESTABLECIMIENTOS_DOCENTES.COMPANIA = V_ACUMULADOS.COMPANIA
    AND ESTABLECIMIENTOS_DOCENTES.CODIGO = PERSONAL.CODIGO_ESTABLECIMIENTO
    LEFT JOIN FUENTE_RECURSOS
 --CA
    ON FUENTE_RECURSOS.COMPANIA = V_ACUMULADOS.COMPANIA
    AND FUENTE_RECURSOS.CODIGO = PERSONAL.FUENTE_DE_RECURSO
    AND FUENTE_RECURSOS.ANO = V_ACUMULADOS.ANO
    INNER JOIN REFERENCIA
    ON REFERENCIA.COMPANIA = V_ACUMULADOS.COMPANIA
    AND REFERENCIA.CODIGO = PERSONAL.REFERENCIA
    AND REFERENCIA.ANO = V_ACUMULADOS.ANO
    INNER JOIN V_FONDO_DE_SALUD
    ON V_FONDO_DE_SALUD.COMPANIA = V_ACUMULADOS.COMPANIA
    AND V_FONDO_DE_SALUD.FONDO_SALUD = PERSONAL.FONDO_SALUD
    INNER JOIN V_FONDO_DE_PENSIONES
    ON V_FONDO_DE_PENSIONES.COMPANIA = V_ACUMULADOS.COMPANIA
    AND V_FONDO_DE_PENSIONES.ID_DEL_FONDO = PERSONAL.ID_DEL_FONDO
    WHERE V_ACUMULADOS.COMPANIA             = s$compania$s
    AND V_ACUMULADOS.ID_DE_PROCESO          = s$proceso$s
    AND V_ACUMULADOS.ANO                    = s$ano$s
    AND V_ACUMULADOS.MES                    = s$mes$s
    AND V_ACUMULADOS.PERIODO                = s$periodo$s
    AND (V_ACUMULADOS.CLASE BETWEEN 3 AND 8
    OR V_ACUMULADOS.ID_DE_CONCEPTO IN (1,9,11) )
    ORDER BY NOMBRE_EMPLEADO
    ) PIVOT (SUM(VALOR) FOR NOMBRECONCEPTO IN (s$condicionPivot$s))
  )SUB
INNER JOIN SUMACUMULADOS
ON SUB.COMPANIA        = SUMACUMULADOS.COMPANIA
AND SUB.ID_DE_EMPLEADO = SUMACUMULADOS.ID_DE_EMPLEADO