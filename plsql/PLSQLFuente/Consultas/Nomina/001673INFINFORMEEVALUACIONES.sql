SELECT  E.CONSECUTIVO,
         E.FECHA,
         E.DESCRIPCION,
         E.FECHA_INICIAL_EVALUACION,
         E.FECHA_FINAL_EVALUACION,
         EE.NOMBRE AS NOMBREESCALAFONEVALUADO,
         EER.NOMBRE AS NOMBREESCALAFONEVALUADOR,
         CER.NOMBRE_DEL_CARGO AS NOMBRECARGOEVALUADOR,
         CE.NOMBRE_DEL_CARGO AS NOMBRECARGOEVALUADO,
         PER.NOMBRECOMPLETO AS NOMBRESEVALUADOR,
         PE.NOMBRECOMPLETO AS NOMBRESEVALUADO,
         CEV.NOMBRE AS CRITERIOEVALUADO,
         SDE.FECHA FECHA_SUB_DETALLE,
         SDE.HORA,
         SDE.CEDULA_EVALUADO,
         SDE.CEDULA_EVALUADOR,
         SDE.PUNTAJE,
         SDE.TEXTO,
         TE.NOMBRE AS TIPOEVALUACION,
         CASE WHEN SDE.VALOR_ASIGNADO NOT IN (0) THEN 'X' ELSE ' ' END  AS ITEMSELECCIONADO,
         SDE.CRITERIO_EVALUADO
  FROM EV_EVALUACIONES E
  INNER JOIN EV_DETALLE_EVALUACION DE 
     ON E.COMPANIA = DE.COMPANIA
    AND E.CLASE_EVALUACION = DE.CLASE_EVALUACION
    AND E.CONSECUTIVO = DE.NUMERO_EVALUACION    
  INNER JOIN EV_SUBDETALLE_EVALUACION SDE 
     ON DE.COMPANIA = SDE.COMPANIA
    AND DE.ESCALAFON_EVALUADO = SDE.ESCALAFON_EVALUADO
    AND DE.ESCALAFON_EVALUADOR = SDE.ESCALAFON_EVALUADOR
    AND DE.CLASE_EVALUACION = SDE.CLASE_EVALUACION
    AND DE.CARGO_EVALUADO = SDE.CARGO_EVALUADO
    AND DE.CARGO_EVALUADOR = SDE.CARGO_EVALUADOR
    AND DE.NUMERO_EVALUACION = SDE.EVALUACION
  INNER JOIN ESCALAFON EE 
     ON SDE.COMPANIA = EE.COMPANIA
    AND SDE.ESCALAFON_EVALUADO = EE.CODIGO
  INNER JOIN ESCALAFON EER 
     ON SDE.COMPANIA = EER.COMPANIA
    AND SDE.ESCALAFON_EVALUADOR = EER.CODIGO
  INNER JOIN CARGOS CE 
     ON SDE.COMPANIA = CE.COMPANIA
    AND SDE.CARGO_EVALUADO = CE.ID_DE_CARGO
    AND SDE.ESCALAFON_EVALUADO = CE.ESCALAFON
  INNER JOIN CARGOS CER 
     ON SDE.COMPANIA = CER.COMPANIA
    AND SDE.ESCALAFON_EVALUADOR = CER.ESCALAFON
    AND SDE.CARGO_EVALUADOR = CER.ID_DE_CARGO
  INNER JOIN PERSONAL PE
     ON DE.COMPANIA = PE.COMPANIA 
    AND DE.CARGO_EVALUADO = PE.ID_DE_CARGO
    AND DE.ESCALAFON_EVALUADO = PE.ESCALAFON
    AND DE.CEDULA_EVALUADO = PE.NUMERO_DCTO
    AND DE.SUCURSAL_EVALUADO = PE.SUCURSAL
    AND DE.CODIGO_EMPLEADO_EVALUADO = PE.ID_DE_EMPLEADO
  INNER JOIN PERSONAL PER
     ON DE.COMPANIA = PER.COMPANIA
    AND DE.ESCALAFON_EVALUADOR = PER.ESCALAFON
    AND DE.CARGO_EVALUADOR = PER.ID_DE_CARGO
    AND DE.CEDULA_EVALUADOR = PER.NUMERO_DCTO
    AND DE.SUCURSAL_EVALUADOR = PER.SUCURSAL
    AND DE.CODIGO_EMPLEADO_EVALUADOR = PER.ID_DE_EMPLEADO
  INNER JOIN EV_CRITERIOS_EVALUACION CEV
     ON CEV.COMPANIA = SDE.COMPANIA
    AND SDE.CRITERIO_EVALUADO = CEV.CODIGO
    AND SDE.CLASE_EVALUACION = CEV.CLASE_EVALUACION 
    AND SDE.COMPANIA = CEV.COMPANIA
  INNER JOIN EV_TIPO_DE_EVALUACION TE 
     ON TE.COMPANIA = SDE.COMPANIA
    AND SDE.TIPO_EVALUACION = TE.CODIGO
    AND SDE.CLASE_EVALUACION = TE.CLASE_EVALUACION
    AND SDE.COMPANIA = TE.COMPANIA
  WHERE E.COMPANIA = s$compania$s
    AND SDE.CLASE_EVALUACION = 's$claseEvaluacion$s'
  AND E.CONSECUTIVO BETWEEN 's$evaluacionInicial$s' AND 's$evaluacionfinal$s'
  AND E.FECHA BETWEEN s$fechaIni$s AND s$fechaFin$s
  AND SDE.CARGO_EVALUADOR BETWEEN 's$cargoEvaluadorInicial$s' AND 's$cargoEvaluadorFinal$s'
  AND SDE.CARGO_EVALUADO BETWEEN 's$cargoEvaluadoInicial$s' AND 's$cargoEvaluadoFinal$s'
  AND SDE.ESCALAFON_EVALUADO BETWEEN 's$escalafonInicialEvaluado$s' AND 's$escalafonFinalEvaluado$s'
  AND SDE.CRITERIO_EVALUADO BETWEEN s$criterioInicial$s AND s$criterioFinal$s
