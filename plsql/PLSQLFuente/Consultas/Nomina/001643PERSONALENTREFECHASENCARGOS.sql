SELECT DISTINCT V_PERSONAL.ID_DE_EMPLEADO,
  V_PERSONAL.APELLIDO1
  ||' '
  ||V_PERSONAL.APELLIDO2
  ||' '
  ||V_PERSONAL.NOMBRES NOMCOMPLETO,
  V_PERSONAL.NUMERO_DCTO,
  V_PERSONAL.FECHA_DE_INGRESO,
  V_PERSONAL.FECHA_DE_RETIRO,
  V_PERSONAL.NOMBRE_ESCALAFON,
  CARGOS.NOMBRE_DEL_CARGO,
  V_PERSONAL.ID_DE_CARGO CODIGO,
  CAT.GRADO,
  ENCARGOS.ID_DE_CARGO ID_DE_CARGOENCARGO,
  ENCARGOS.FECHAINICIO,
  ENCARGOS.FECHAFINAL,
  V_PERSONAL.SEXO,
  V_PERSONAL.DE_CARRERA,
  V_PERSONAL.NOMBRE_DEPENDENCIA,
  PCK_SYSMAN_UTL.FC_DIASMESCOMERCIAL(UN_FECHAIN => ENCARGOS.FECHAINICIO,UN_FECHAFIN => ENCARGOS.FECHAFINAL)DIASENCARGO
FROM V_PERSONAL
  INNER JOIN CATEGORIA CAT
     ON V_PERSONAL.COMPANIA        = CAT.COMPANIA
    AND V_PERSONAL.ESCALAFON       = CAT.ESCALAFON
    AND V_PERSONAL.ID_DE_CATEGORIA = CAT.ID_DE_CATEGORIA
    AND V_PERSONAL.ANO             = CAT.ANO
    INNER JOIN ENCARGOS
       ON V_PERSONAL.COMPANIA        = ENCARGOS.COMPANIA
      AND V_PERSONAL.ID_DE_EMPLEADO  = ENCARGOS.ID_DE_EMPLEADO
      INNER JOIN CARGOS
         ON ENCARGOS.COMPANIA    = CARGOS.COMPANIA
        AND ENCARGOS.ID_DE_CARGO = CARGOS.ID_DE_CARGO
WHERE V_PERSONAL.COMPANIA          = s$compania$s
AND V_PERSONAL.ID_DE_PROCESO       = s$procesoNomina$s
AND V_PERSONAL.ANO                 = s$anoNomina$s
AND V_PERSONAL.MES                 = s$mesNomina$s
AND V_PERSONAL.PERIODO             = s$periodoNomina$s
AND V_PERSONAL.ESTADO_ACTUAL       = s$estadoActual$s
AND V_PERSONAL.ID_DE_EMPLEADO NOT IN (0)
AND V_PERSONAL.SEXO                =
  CASE
    WHEN 's$genero$s' = 'T'
    THEN V_PERSONAL.SEXO
    ELSE 's$genero$s'
  END
AND V_PERSONAL.ID_DE_CARGO BETWEEN 's$cargoInicial$s' AND 's$cargoFinal$s'
AND V_PERSONAL.DE_CARRERA BETWEEN 's$tipoContratoInicial$s' AND 's$tipoContratoFinal$s'
AND ENCARGOS.FECHAINICIO BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
AND V_PERSONAL.FECHANCTO BETWEEN TO_DATE('s$fechaInicialNacimiento$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinalNacimiento$s','DD/MM/YYYY')
