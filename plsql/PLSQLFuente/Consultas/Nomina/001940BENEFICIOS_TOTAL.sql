SELECT PROVISIONES_MENSUALES_NIIF.COMPANIA,
  PERSONAL.NIVELSIIF,
  PERSONAL.ID_CENTRO_DE_COSTO,
  PERSONAL.NOMBRECOMPLETO,
  CARGOS.NOMBRE_DEL_CARGO,
  SUBSTR(CARGOS.ID_DE_CARGO,1,2) AS COD,
  CATEGORIA.GRADO,
  PERSONAL.FECHA_DE_INGRESO,
  PROVISIONES_MENSUALES_NIIF.ANO,
  PCK_SYSMAN_UTL.FC_NOMBRE_MES(PROVISIONES_MENSUALES_NIIF.MES) AS MES,
  PROVISIONES_MENSUALES_NIIF.PERIODO,
  PROVISIONES_MENSUALES_NIIF.ID_DE_EMPLEADO,
  PROVISIONES_MENSUALES_NIIF.DCTO_IDENTIDAD,
  PROVISIONES_MENSUALES_NIIF.PS_TOTAL,
  PROVISIONES_MENSUALES_NIIF.PS_PRV,
  PROVISIONES_MENSUALES_NIIF.PV_TOTAL,
  PROVISIONES_MENSUALES_NIIF.PV_PRV,
  PROVISIONES_MENSUALES_NIIF.VACD_TOTAL,
  PROVISIONES_MENSUALES_NIIF.VACD_PRV,
  PROVISIONES_MENSUALES_NIIF.BER_TOTAL,
  PROVISIONES_MENSUALES_NIIF.BER_PRV,
  PROVISIONES_MENSUALES_NIIF.PN_TOTAL,
  PROVISIONES_MENSUALES_NIIF.PN_PRV,
  PROVISIONES_MENSUALES_NIIF.CES_TOTAL,
  PROVISIONES_MENSUALES_NIIF.CES_PRV,
  PROVISIONES_MENSUALES_NIIF.ICES_TOTAL,
  PROVISIONES_MENSUALES_NIIF.ICES_PRV,
  PROVISIONES_MENSUALES_NIIF.BASP_TOTAL,
  PROVISIONES_MENSUALES_NIIF.BASP_PRV
FROM PROVISIONES_MENSUALES_NIIF
INNER JOIN PERSONAL
ON PROVISIONES_MENSUALES_NIIF.COMPANIA      = PERSONAL.COMPANIA
AND PROVISIONES_MENSUALES_NIIF.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
INNER JOIN DEPENDENCIA
ON PERSONAL.COMPANIA   = DEPENDENCIA.COMPANIA
AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
INNER JOIN CARGOS
ON PERSONAL.COMPANIA   = CARGOS.COMPANIA
AND PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO
INNER JOIN CATEGORIA
ON PERSONAL.COMPANIA           = CATEGORIA.COMPANIA
AND PERSONAL.ESCALAFON         = CATEGORIA.ESCALAFON
AND PERSONAL.ID_DE_CATEGORIA   = CATEGORIA.ID_DE_CATEGORIA
AND PERSONAL.ANO               = CATEGORIA.ANO
WHERE PROVISIONES_MENSUALES_NIIF.COMPANIA  = s$compania$s
AND PROVISIONES_MENSUALES_NIIF.ANO         = s$ano$s
AND PROVISIONES_MENSUALES_NIIF.MES         = s$mes$s
AND PROVISIONES_MENSUALES_NIIF.PERIODO     = s$periodo$s
AND PROVISIONES_MENSUALES_NIIF.ID_DE_EMPLEADO NOT IN ('0')
AND CATEGORIA.ANO                          = PERSONAL.ANO
ORDER BY PERSONAL.ID_CENTRO_DE_COSTO,
  PERSONAL.NOMBRECOMPLETO
