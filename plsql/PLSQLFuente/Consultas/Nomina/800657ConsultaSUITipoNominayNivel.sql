MERGE INTO CONSULTAS FIN USING (SELECT '800657ConsultaSUITipoNominayNivel' INFORME ,TO_CLOB(q'[SELECT DISTRIBUCION.COMPANIA, 
DISTRIBUCION.ID_CENTRO_DE_COSTO, 
CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO, 
CASE personal.SUI_TIPO_NOM WHEN 'PN' THEN 'Personal Nómina' WHEN 'PT' THEN 'Personal Temporal' WHEN 'PC' THEN 'Personal Contratista' ELSE 'Vacantes' END AS SUI_TIPO_NOM1, 
CASE personal.SUI_NIVEL WHEN 'ND' THEN 'Nivel Directivo' WHEN 'NA' THEN 'Nivel Administrativo' ELSE 'Nivel Técnico-Operativo' END AS SUI_NIVEL1, 
SUM(CASE WHEN CONCEPTOS.FACTOR_SUELDOS = -1 THEN DISTRIBUCION.VALOR ELSE 0 END) AS FACTOR_SUELDOS, 
SUM(CASE WHEN CONCEPTOS.FACTOR_SALARIAL = -1 THEN DISTRIBUCION.VALOR ELSE 0 END) AS FACTOR_SALARIAL, 
SUM(CASE WHEN CONCEPTOS.FACTOR_LEGALES = -1 THEN DISTRIBUCION.VALOR ELSE 0 END) AS FACTOR_LEGALES, 
SUM(CASE WHEN CONCEPTOS.FACTOR_EXTRALEGALES = -1 THEN DISTRIBUCION.VALOR ELSE 0 END) AS FACTOR_EXTRALEGALES, 
SUM(CASE WHEN CONCEPTOS.FACTOR_OTROSPAGOS = -1 THEN DISTRIBUCION.VALOR ELSE 0 END) AS FACTOR_OTROSPAGOS, 
PERSONAL.SUI_TIPO_NOM, PERSONAL.SUI_NIVEL
FROM DISTRIBUCION 
LEFT JOIN CONCEPTOS 
ON DISTRIBUCION.COMPANIA = CONCEPTOS.COMPANIA
AND DISTRIBUCION.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
LEFT JOIN PERIODOS 
ON DISTRIBUCION.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO
AND DISTRIBUCION.COMPANIA = PERIODOS.COMPANIA
AND DISTRIBUCION.ANO = PERIODOS.ANO
AND DISTRIBUCION.MES = PERIODOS.MES 
AND DISTRIBUCION.PERIODO = PERIODOS.PERIODO
LEFT JOIN PERSONAL 
ON DISTRIBUCION.COMPANIA = PERSONAL.COMPANIA
AND DISTRIBUCION.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
LEFT JOIN CENTRO_COSTO 
ON DISTRIBUCION.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
AND DISTRIBUCION.ANO = CENTRO_COSTO.ANO
AND DISTRIBUCION.COMPANIA = CENTRO_COSTO.COMPANIA
WHERE DISTRIBUCION.COMPANIA = s$compania$s
AND DISTRIBUCION.ano || DISTRIBUCION.mes || DISTRIBUCION.periodo Between s$anio1$s||s$mes1$s||s$periodo1$s And s$anio2$s||s$mes2$s||s$periodo2$s 
AND (CONCEPTOS.Clase = 3 
Or CONCEPTOS.Clase = 8) 
AND PERIODOS.ACUMULADO NOT IN (0)
group by DISTRIBUCION.COMPANIA, DISTRIBUCION.ID_CENTRO_DE_COSTO, CENTRO_COSTO.NOMBRE, CASE personal.SUI_TIPO_NOM WHEN 'PN' THEN 'Personal Nómina' WHEN 'PT' THEN 'Personal Temporal' WHEN 'PC' THEN 'Personal Contratista' ELSE 'Vacantes' END, CASE personal.SUI_NIVEL WHEN 'ND' THEN 'Nivel Directivo' WHEN 'NA' THEN 'Nivel Administrativo' ELSE 'Nivel Técnico-Operativo' END, 
PERSONAL.SUI_TIPO_NOM, PERSONAL.SUI_NIVEL
HAVING Sum(conceptos.FACTOR_SUELDOS+conceptos.FACTOR_SALARIAL+conceptos.FACTOR_PRESTACIONAL+conceptos.FACTOR_EXTRALEGALES+conceptos.FACTOR_LEGALES) <> 0
ORDER BY DISTRIBUCION.ID_CENTRO_DE_COSTO]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);