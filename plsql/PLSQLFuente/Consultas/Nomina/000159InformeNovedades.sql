MERGE INTO CONSULTAS FIN USING (SELECT '000159InformeNovedades' INFORME ,TO_CLOB(q'[ SELECT
  PER.COMPANIA AS COMPANIA,
  PER.ID_DE_EMPLEADO AS ID_DE_EMPLEADO,
  PER.NOMBRECOMPLETO AS NOMBRECOMPLETO,
  PER.NUMERO_DCTO AS NUMERO_DCTO,
  PER.FECHA_DE_INGRESO,
  NOVEDADES.ID_DE_CONCEPTO AS ID_DE_CONCEPTO,
  CONCEPTOS.NOMBRE_CONCEPTO AS NOMBRE_CONCEPTO,
  NOVEDADES.FECHA AS FECHA,
  NOVEDADES.VALOR AS VAL
FROM NOVEDADES 
INNER JOIN PERSONAL PER
      ON NOVEDADES.COMPANIA        = PER.COMPANIA
      AND NOVEDADES.ID_DE_EMPLEADO = PER.ID_DE_EMPLEADO
INNER JOIN CONCEPTOS
     ON CONCEPTOS.COMPANIA        = NOVEDADES.COMPANIA
     AND CONCEPTOS.ID_DE_CONCEPTO = NOVEDADES.ID_DE_CONCEPTO
INNER JOIN PERIODOS
        ON NOVEDADES.COMPANIA        = PERIODOS.COMPANIA
        AND NOVEDADES.ID_DE_PROCESO  = PERIODOS.ID_DE_PROCESO
        AND NOVEDADES.MES            = PERIODOS.MES
        AND NOVEDADES.ANO            = PERIODOS.ANO
        AND NOVEDADES.PERIODO        = PERIODOS.PERIODO
WHERE NOVEDADES.ID_DE_EMPLEADO <> 0
  AND NOVEDADES.COMPANIA = s$compania$s
 AND NOVEDADES.ID_DE_EMPLEADO = CASE WHEN s$opcion$s = 3 THEN s$empleado$s ELSE NOVEDADES.ID_DE_EMPLEADO END
  AND (LPAD(NOVEDADES.ID_DE_PROCESO,2,'0') || LPAD(NOVEDADES.ANO, 4, '0') || LPAD(NOVEDADES.MES, 2,'0') || LPAD(NOVEDADES.PERIODO,2,'0'))
  BETWEEN s$intervalo1$s 
      AND s$intervalo2$s
 ORDER BY NOVEDADES.ID_DE_EMPLEADO]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);