SELECT
    HISTORICOS.ID_DE_PROCESO,
    HISTORICOS.ANO,
    PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES) MES,
    PERSONAL.ID_DE_EMPLEADO,
    HISTORICOS.ID_DE_CONCEPTO,
    PERIODOS.NOM_PERIODO,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '227' THEN HISTORICOS.VALOR ELSE 0 END) AS C227,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '957' THEN HISTORICOS.VALOR ELSE 0 END)  AS C957,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '986' THEN HISTORICOS.VALOR ELSE 0 END)  AS C986,
    CASE WHEN HISTORICOS.ID_DE_CONCEPTO     = '227' THEN V_FONDO_DE_SALUD.NIT ELSE V_FONDO_DE_PENSIONES.NIT END  AS NIT,
    CASE WHEN HISTORICOS.ID_DE_CONCEPTO     = '227' THEN V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD ELSE V_FONDO_DE_PENSIONES.NOMBRE_DEL_FONDO END AS NOMBRE_FONDO,
    CENTRO_COSTO.CODIGO AS ID_CENTRO_DE_COSTO,
    CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO,
    DEPENDENCIA.CODIGO AS DEPENDENCIA,
    DEPENDENCIA.NOMBRE  AS NOMBREDEPENDENCIA
FROM HISTORICOS
LEFT JOIN PERSONAL
    ON PERSONAL.COMPANIA = HISTORICOS.COMPANIA
    AND PERSONAL.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
LEFT JOIN PERIODOS
    ON PERIODOS.COMPANIA = HISTORICOS.COMPANIA
    AND PERIODOS.ID_DE_PROCESO = HISTORICOS.ID_DE_PROCESO
	AND PERIODOS.ANO = HISTORICOS.ANO
	AND PERIODOS.MES = HISTORICOS.MES
	AND PERIODOS.PERIODO = HISTORICOS.PERIODO
LEFT JOIN V_FONDO_DE_PENSIONES
    ON V_FONDO_DE_PENSIONES.COMPANIA     = PERSONAL.COMPANIA
	AND V_FONDO_DE_PENSIONES.ID_DEL_FONDO = PERSONAL.ID_DEL_FONDO
LEFT JOIN V_FONDO_DE_SALUD 
    ON PERSONAL.COMPANIA =  V_FONDO_DE_SALUD.COMPANIA
	AND PERSONAL.FONDO_SALUD = V_FONDO_DE_SALUD.FONDO_SALUD
LEFT JOIN CENTRO_COSTO
    ON CENTRO_COSTO.COMPANIA = PERSONAL.COMPANIA
    AND CENTRO_COSTO.CODIGO = PERSONAL.ID_CENTRO_DE_COSTO
    AND CENTRO_COSTO.ANO = HISTORICOS.ANO
LEFT JOIN DEPENDENCIA
    ON DEPENDENCIA.COMPANIA = PERSONAL.COMPANIA
    AND DEPENDENCIA.CODIGO = PERSONAL.DEPENDENCIA
WHERE   HISTORICOS.COMPANIA           = s$compania$s
	AND   HISTORICOS.ANO                = s$anioNomina$s
	AND   HISTORICOS.MES                = s$mesNomina$s
	AND   HISTORICOS.PERIODO            = s$periodoNomina$s
	AND   HISTORICOS.ID_DE_PROCESO      = s$procesoNomina$s
	AND   HISTORICOS.ID_DE_CONCEPTO IN ('227','957','986')
GROUP BY 
    HISTORICOS.ID_DE_PROCESO,
    HISTORICOS.ANO,
    HISTORICOS.MES,
    PERSONAL.ID_DE_EMPLEADO,
    HISTORICOS.ID_DE_CONCEPTO,
    PERIODOS.NOM_PERIODO,
    V_FONDO_DE_SALUD.NIT,
    V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD,
    V_FONDO_DE_PENSIONES.NIT,
    V_FONDO_DE_PENSIONES.NOMBRE_DEL_FONDO,
    CENTRO_COSTO.CODIGO,
    CENTRO_COSTO.NOMBRE,
    DEPENDENCIA.CODIGO,
    DEPENDENCIA.NOMBRE