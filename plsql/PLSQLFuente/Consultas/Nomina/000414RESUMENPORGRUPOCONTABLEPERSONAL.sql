SELECT DISTINCT V_ACUMULADOS.ID_DE_CONCEPTO,
      V_ACUMULADOS.NOMBRE_CONCEPTO, 
      V_ACUMULADOS.CLASE, 
      V_ACUMULADOS.GRUPOCONTABLE_NOM GRUPOCONTABLE,
 SUM(V_ACUMULADOS.VALOR) SUMA_DE_VALOR, 
 CONCEPTOS.RELACIONADO,
 CLASE_NOMBRE_MAYU CLASECONCEPTO
FROM V_ACUMULADOS
INNER JOIN CONCEPTOS 
  ON V_ACUMULADOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO 
  AND V_ACUMULADOS.COMPANIA = CONCEPTOS.COMPANIA
WHERE V_ACUMULADOS.COMPANIA= s$compania$s
AND CONCEPTOS.CLASE IN (3, 4, 5, 6, 7, 8)
AND  LPAD(V_ACUMULADOS.ID_DE_PROCESO, 2, 0) || LPAD(V_ACUMULADOS.ANO, 4, 0) || LPAD(V_ACUMULADOS.MES, 2, 0) || LPAD(V_ACUMULADOS.PERIODO, 2, 0) BETWEEN 's$fechaInicial$s'  AND 's$fechaFinal$s' 
AND V_ACUMULADOS.PERIODO<>0 
group by V_ACUMULADOS.ID_DE_CONCEPTO,
V_ACUMULADOS.NOMBRE_CONCEPTO,
V_ACUMULADOS.CLASE,
V_ACUMULADOS.GRUPOCONTABLE_NOM,
CONCEPTOS.RELACIONADO, 
CLASE_NOMBRE_MAYU 
ORDER BY V_ACUMULADOS.ID_DE_CONCEPTO
