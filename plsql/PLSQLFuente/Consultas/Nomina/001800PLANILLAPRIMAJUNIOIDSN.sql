SELECT HISTORICOS.COMPANIA,
  PERSONAL.ID_CENTRO_DE_COSTO,
  TRIM(PERSONAL.APELLIDO1)
  || ' '
  || TRIM(PERSONAL.APELLIDO2)
  || ' '
  || TRIM(PERSONAL.NOMBRES) AS NOMCOMPLETO,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  DEPENDENCIA.NOMBRE AS DPTO,
  CASE PERSONAL.GRUPOCONTABLE
    WHEN 'A'
    THEN 'ADMNISTRATIVO'
    WHEN 'V'
    THEN 'OPERATIVO'
    WHEN 'A'
    THEN 'PRODUCCION'
    ELSE 'OTRO'
  END GRUPOCONTABLE,
  DEPENDENCIA.CODIGO AS DEPENDENCIA,
  CATEGORIA.CODIGOGRADO,
  HISTORICOS.ID_DE_EMPLEADO,
  PERSONAL.NUMERO_DCTO AS NUMERO_DCTO,
  PERSONAL.FECHA_DE_INGRESO,
  DEPENDENCIA.NOMBRE AS NOMBRE_DEPENDENCIA,
  PERSONAL.ID_DE_CARGO,
  CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='160'
    THEN HISTORICOS.VALOR
    ELSE 0
  END)+ SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO =''
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS PRIMAJUN,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO =''
    THEN HISTORICOS.VALOR
    ELSE 0
  END) + SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='159'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS PRIMAJUNPES,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='158'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS PRIMANAV,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='945'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS SUELDOJUN,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='950'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS GASTOSREP,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='991'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS PA,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='946'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS BON,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='947'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS EXTRASJUN,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='948'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS TRANSPJUN,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='953'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS LICENCIAS,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='949'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS ALIMENJUN,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='954'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS VPA,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='950'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS PLOCALJUN,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='951'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS DIASPJUN,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='952'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS DIASCOMJUN,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='945'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) + SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='948'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) + SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='947'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS BASE,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='140'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS DESCUENTOS,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='097'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS DEVENGOS,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='144'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS NETO,
  CARGOS.NOMBRE_DEL_CARGO,
  PERSONAL.ID_DE_TIPO,
  CASE
    WHEN PERSONAL.FECHA_DE_INGRESO > TO_DATE('01/01/'
      || EXTRACT(YEAR FROM SYSDATE) ,'DD/MM/YYYY')
    THEN PERSONAL.FECHA_DE_INGRESO
    ELSE TO_DATE('01/01/'
      || EXTRACT(YEAR FROM SYSDATE),'DD/MM/YYYY')
  END FECHA_DE_INGRESOMALLAMAS,
  CASE
    WHEN PERSONAL.FECHATERCONTRATO > TO_DATE('30/06/'
      || EXTRACT(YEAR FROM SYSDATE) ,'DD/MM/YYYY')
    THEN TO_DATE('30/06/'
      || EXTRACT(YEAR FROM SYSDATE),'DD/MM/YYYY')
    ELSE PERSONAL.FECHATERCONTRATO
  END FECHATERCONTRATO,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='954'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS AUX_EXP,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='956'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS AUX_EDU,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='951'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS DOCEAVAS,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO ='941'
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS OTROS_FACTORES
FROM HISTORICOS
INNER JOIN PERSONAL
ON HISTORICOS.COMPANIA        = PERSONAL.COMPANIA
AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
INNER JOIN DEPENDENCIA
ON PERSONAL.COMPANIA     = DEPENDENCIA.COMPANIA
AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
INNER JOIN CATEGORIA
ON PERSONAL.COMPANIA         = CATEGORIA.COMPANIA
AND PERSONAL.ESCALAFON       = CATEGORIA.ESCALAFON
AND PERSONAL.ID_DE_CATEGORIA = CATEGORIA.ID_DE_CATEGORIA
AND PERSONAL.ANO             = CATEGORIA.ANO
INNER JOIN CARGOS
ON PERSONAL.COMPANIA     = CARGOS.COMPANIA
AND PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO
INNER JOIN CENTRO_COSTO
ON PERSONAL.COMPANIA            = CENTRO_COSTO.COMPANIA
AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
AND PERSONAL.ANO = CENTRO_COSTO.ANO
WHERE PERSONAL.COMPANIA           = s$compania$s
  AND HISTORICOS.ID_DE_PROCESO    = s$idProceso$s
  AND HISTORICOS.ANO              = s$ano$s
  AND HISTORICOS.MES              = s$mes$s
  AND HISTORICOS.PERIODO          = s$periodo$s
  AND HISTORICOS.ID_DE_CONCEPTO  IN ('160','097','140','144','945','946','947','948','949','950','951','952','953','954','955','956','158','160','159','956','991','941')
GROUP BY HISTORICOS.COMPANIA,
  PERSONAL.ID_CENTRO_DE_COSTO,
  TRIM(PERSONAL.APELLIDO1)
  || ' '
  || TRIM(PERSONAL.APELLIDO2)
  || ' '
  || TRIM(PERSONAL.NOMBRES),
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  CASE PERSONAL.GRUPOCONTABLE
    WHEN 'A'
    THEN 'ADMNISTRATIVO'
    WHEN 'V'
    THEN 'OPERATIVO'
    WHEN 'A'
    THEN 'PRODUCCION'
    ELSE 'OTRO'
  END,
  DEPENDENCIA.CODIGO,
  CATEGORIA.CODIGOGRADO,
  PERSONAL.NUMERO_DCTO,
  PERSONAL.FECHA_DE_INGRESO,
  DEPENDENCIA.NOMBRE,
  PERSONAL.ID_DE_CARGO,
  CENTRO_COSTO.NOMBRE,
  CARGOS.NOMBRE_DEL_CARGO,
  PERSONAL.ID_DE_TIPO,
  CASE
    WHEN PERSONAL.FECHATERCONTRATO > TO_DATE('30/06/'
      || EXTRACT(YEAR FROM SYSDATE) ,'DD/MM/YYYY')
    THEN TO_DATE('30/06/'
      || EXTRACT(YEAR FROM SYSDATE),'DD/MM/YYYY')
    ELSE PERSONAL.FECHATERCONTRATO
  END,
  HISTORICOS.ID_DE_EMPLEADO
HAVING SUM(
            CASE
              WHEN HISTORICOS.ID_DE_CONCEPTO = '097'
              THEN HISTORICOS.VALOR
              ELSE 0
            END) <> 0
ORDER BY PERSONAL.ID_CENTRO_DE_COSTO,
  TRIM(PERSONAL.APELLIDO1)
  || ' '
  || TRIM(PERSONAL.APELLIDO2)
  || ' '
  || TRIM(PERSONAL.NOMBRES) 
