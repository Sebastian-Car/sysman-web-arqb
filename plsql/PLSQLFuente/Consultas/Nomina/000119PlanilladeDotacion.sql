SELECT PERSONAL.NOMBRECOMPLETO  NOMBRECOMPLETO,
       PERSONAL.ID_DE_EMPLEADO ID_DE_EMPLEADO,
       PERSONAL.FECHA_DE_INGRESO FECHA_DE_INGRESO,
       PERSONAL.NUMERO_DCTO NUMERO_DCTO,
       PERSONAL.CHAQUETA CHAQUETA,
       PERSONAL.CAMISA CAMISA,
       PERSONAL.CALZADO CALZADO,
       PERSONAL.PANTALON PANTALON,
       PERSONAL.DEPENDENCIA DEPENDENCIA,
       CASE WHEN 
         PERSONAL.SEXO = 'M'
       THEN 
         'MASCULINO'
       ELSE
         'FEMENINO'
       END  SEX, 
       CARGOS.NOMBRE_DEL_CARGO, 
       CATEGORIA.NOMBRE_CATEGORIA, 
       CATEGORIA.SALARIO_BASE  SALARIO_BASE, 
       CATEGORIA.SALARIO_BASE * CATEGORIA.GASTOSREPRESENTACION / 100  GASTOS, 
       ESCALAFON.NOMBRE, 
       ESCALAFON.CODIGO, 
       CASE WHEN 
         PERSONAL.SEXO = 'M'
       THEN 
         1
       ELSE
         CASE WHEN 
           PERSONAL.SEXO = 'F'
         THEN 
           2
         ELSE
           3
         END
       END  VALORSEXO, 
       DEPENDENCIA.NOMBRE  NOMBREDEPENDENCIA
FROM PERSONAL 
  LEFT JOIN CARGOS 
    ON  PERSONAL.COMPANIA    = CARGOS.COMPANIA  
    AND PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO
  LEFT JOIN CATEGORIA 
    ON  PERSONAL.COMPANIA        = CATEGORIA.COMPANIA 
    AND PERSONAL.ANO             = CATEGORIA.ANO
    AND PERSONAL.ESCALAFON       = CATEGORIA.ESCALAFON 
    AND PERSONAL.ID_DE_CATEGORIA = CATEGORIA.ID_DE_CATEGORIA 
  LEFT JOIN ESCALAFON 
    ON PERSONAL.COMPANIA    = ESCALAFON.COMPANIA
    AND  PERSONAL.ESCALAFON = ESCALAFON.CODIGO 
  LEFT JOIN DEPENDENCIA
    ON  PERSONAL.COMPANIA    = DEPENDENCIA.COMPANIA
    AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO 
WHERE PERSONAL.COMPANIA      =  s$compania$s
  AND CATEGORIA.ANO         = s$anio$s
  AND PERSONAL.ESTADO_ACTUAL NOT IN (3,2) 
  AND CATEGORIA.SALARIO_BASE <= (2 * (s$salarioMinimo$s))
   AND PCK_SYSMAN_UTL.FC_EDAD(s$date$s  , PERSONAL.FECHA_DE_INGRESO, 3) >= 90 
   AND PERSONAL.ID_DE_EMPLEADO NOT IN (0)
