SELECT *
FROM
  (SELECT PERSONAL.NOMBRECOMPLETO,
    HISTORICOS.ID_DE_EMPLEADO,
    HISTORICOS.ANO AS ANO,
    HISTORICOS.MES,
    NVL(CONCEPTOS.COLUMNA_SIDEF11,'<>') AS COLUMNA_SIDEF11,
    HISTORICOS.VALOR
  FROM HISTORICOS
  INNER JOIN CONCEPTOS
  ON (HISTORICOS.COMPANIA        = CONCEPTOS.COMPANIA)
  AND (HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO)
  INNER JOIN PERSONAL
  ON (HISTORICOS.COMPANIA        = PERSONAL.COMPANIA)
  AND (HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO)
  WHERE HISTORICOS.COMPANIA      =s$compania$s
  AND HISTORICOS.ANO
    || LPAD(HISTORICOS.MES,2,'0')
    || LPAD(HISTORICOS.PERIODO,2,'0') BETWEEN s$ano1$s
    || LPAD(s$mes1$s,2,'0')
    || LPAD(s$periodo1$s,2,'0')
  AND s$ano2$s
    || LPAD(s$mes2$s,2,'0')
    || LPAD(s$periodo2$s,2,'0')
  AND ((CONCEPTOS.CLASE          =3)
  OR (CONCEPTOS.CLASE            =5
  AND HISTORICOS.ID_DE_CONCEPTO IN (130,131,132,120))
  OR (CONCEPTOS.CLASE            =8
  AND HISTORICOS.ID_DE_CONCEPTO BETWEEN 101 AND 105) )
  ) PIVOT(SUM(VALOR) FOR COLUMNA_SIDEF11 IN (s$pivot$s))
ORDER BY NOMBRECOMPLETO,
  ANO,
  MES
