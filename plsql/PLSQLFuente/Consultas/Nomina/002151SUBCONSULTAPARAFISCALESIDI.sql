SELECT HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  CONCEPTOS.NOMBRE_CONCEPTO,
  SUM(HISTORICOS.VALOR) AS SUMADEVALOR,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ID_DE_CONCEPTO
FROM HISTORICOS
INNER JOIN CONCEPTOS
   ON CONCEPTOS.COMPANIA = HISTORICOS.COMPANIA
   AND CONCEPTOS.ID_DE_CONCEPTO = HISTORICOS.ID_DE_CONCEPTO
INNER JOIN PERSONAL
   ON PERSONAL.COMPANIA = HISTORICOS.COMPANIA
   AND PERSONAL.ID_DE_EMPLEADO  = HISTORICOS.ID_DE_EMPLEADO
WHERE HISTORICOS.COMPANIA   = s$compania$s
AND HISTORICOS.ANO          =s$anio$s
AND HISTORICOS.MES          =s$mes$s
AND HISTORICOS.PERIODO      =s$periodo$s
AND HISTORICOS.ID_DE_PROCESO = s$proceso$s
AND HISTORICOS.ID_DE_CONCEPTO BETWEEN '101' AND '103'
GROUP BY HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  CONCEPTOS.NOMBRE_CONCEPTO,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ID_DE_CONCEPTO