SELECT HISTORICOS.COMPANIA,
  V_FONDO_DE_SALUD.NIT,
  V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD,
  PERSONAL_HISTORICO.ID_DE_EMPLEADO,
  PERSONAL_HISTORICO.NOMBRECOMPLETO NOMBRECOMPLETO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  PERSONAL_HISTORICO.SALARIO_BASE_IBC SALARIO_BASE_IBC,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=370
    AND INCAPACIDADES.INCAPACIDAD<>04
    THEN HISTORICOS.VALOR
    ELSE 0
  END) C370,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=371
    AND INCAPACIDADES.INCAPACIDAD<>04
    THEN HISTORICOS.VALOR
    ELSE 0
  END) C371,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=372
    AND INCAPACIDADES.INCAPACIDAD<>04
    THEN HISTORICOS.VALOR
    ELSE 0
  END) C372,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=373
    AND INCAPACIDADES.INCAPACIDAD<>04
    THEN HISTORICOS.VALOR
    ELSE 0
  END) C373,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=375
    AND INCAPACIDADES.INCAPACIDAD<>04
    THEN HISTORICOS.VALOR
    ELSE 0
  END) C375,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=376
    AND INCAPACIDADES.INCAPACIDAD<>04
    THEN HISTORICOS.VALOR
    ELSE 0
  END) C376,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=377
    AND INCAPACIDADES.INCAPACIDAD<>04
    THEN HISTORICOS.VALOR
    ELSE 0
  END) C377,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=378
    AND INCAPACIDADES.INCAPACIDAD<>04
    THEN HISTORICOS.VALOR
    ELSE 0
  END) C378,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=348
    THEN HISTORICOS.VALOR
    ELSE 0
  END) C348,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=343
    THEN HISTORICOS.VALOR
    ELSE 0
  END) AS "C343 IBC LMA",
  INCAPACIDADES.INCAPACIDAD,
  PERSONAL_HISTORICO.NOMBRE_TIPO,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=130
    THEN HISTORICOS.VALOR
    ELSE 0
  END) DTOSALUDEMPLEADO,
  V_FONDO_DE_SALUD.FONDO_SALUD,
  INCAPACIDADES.AUTO,
  TO_CHAR(INCAPACIDADES.FECHA_AUTO, 'DD/MM/YYYY') FECHA_AUTO,
  INCAPACIDADES.DIAS,
  TO_CHAR(INCAPACIDADES.FECHAINIREAL, 'DD/MM/YYYY') FECHAINIREAL,
  TO_CHAR(INCAPACIDADES.FECHAFINREAL, 'DD/MM/YYYY')FECHAFINREAL
 FROM HISTORICOS 
 LEFT JOIN PERSONAL_HISTORICO 
	ON HISTORICOS.COMPANIA = PERSONAL_HISTORICO.COMPANIA 
  AND HISTORICOS.ID_DE_PROCESO = PERSONAL_HISTORICO.ID_DE_PROCESO
  AND HISTORICOS.ANO = PERSONAL_HISTORICO.ANO
  AND HISTORICOS.MES = PERSONAL_HISTORICO.MES
  AND HISTORICOS.PERIODO = PERSONAL_HISTORICO.PERIODO
  AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL_HISTORICO.ID_DE_EMPLEADO
 LEFT JOIN V_FONDO_DE_SALUD 
  ON PERSONAL_HISTORICO.COMPANIA = V_FONDO_DE_SALUD.COMPANIA 
	AND PERSONAL_HISTORICO.FONDO_SALUD = V_FONDO_DE_SALUD.FONDO_SALUD
 INNER JOIN INCAPACIDADES 
  ON HISTORICOS.COMPANIA = INCAPACIDADES.COMPANIA 
  AND HISTORICOS.ID_DE_PROCESO = INCAPACIDADES.ID_DE_PROCESO
  AND HISTORICOS.ANO = INCAPACIDADES.ANO
  AND HISTORICOS.MES = INCAPACIDADES.MES
  AND HISTORICOS.PERIODO = INCAPACIDADES.PERIODO
	AND HISTORICOS.ID_DE_EMPLEADO = INCAPACIDADES.ID_DE_EMPLEADO 
  WHERE (HISTORICOS.COMPANIA = s$compania$s
 	AND  HISTORICOS.ID_DE_PROCESO = s$proceso$s
	AND ((LPAD(HISTORICOS.ANO, 4, 0) || LPAD(HISTORICOS.MES, 2, 0) || LPAD(HISTORICOS.PERIODO, 2, 0))  BETWEEN 's$fechaInicial$s'  AND 's$fechaFinal$s')
  AND  HISTORICOS.ID_DE_CONCEPTO IN (370, 371, 372, 373, 374, 375, 376, 377, 378, 379, 348, 001, 343)) 	
	GROUP BY HISTORICOS.COMPANIA, 
 V_FONDO_DE_SALUD.NIT,
 V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD, 
 INCAPACIDADES.INCAPACIDAD, 
 PERSONAL_HISTORICO.NOMBRE_TIPO,
 PERSONAL_HISTORICO.NOMBRECOMPLETO, 
 PERSONAL_HISTORICO.ID_DE_EMPLEADO, 
 V_FONDO_DE_SALUD.FONDO_SALUD,
 PERSONAL_HISTORICO.SALARIO_BASE_IBC,
 INCAPACIDADES.AUTO, 
 INCAPACIDADES.FECHA_AUTO, 
 INCAPACIDADES.DIAS, 
 INCAPACIDADES.FECHAINIREAL, 
 INCAPACIDADES.FECHAFINREAL,
 HISTORICOS.ANO, 
 HISTORICOS.MES
 HAVING (SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=379
             THEN HISTORICOS.VALOR
             ELSE 0
             END)<>0)
