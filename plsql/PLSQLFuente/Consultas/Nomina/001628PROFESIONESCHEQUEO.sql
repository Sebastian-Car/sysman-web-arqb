SELECT 
  DETALLE_PROFESIONES.NOMBRE_PROFESION NOMBRE,
  DETALLE_PROFESIONES.DESCRIPCIONPROF DESCRIPCION,
  CASE DETALLE_PROFESIONES.TIPO
    WHEN 'P'
    THEN 'Profesiones'
    WHEN 'G'
    THEN 'Posgrados'
    WHEN 'M'
    THEN 'Maestrias'
    ELSE 'Otro'
  END TIPO,
  DETALLE_PROFESIONES.ESTABLECIMIENTO INSTITUCION
FROM DETALLE_PROFESIONES
WHERE DETALLE_PROFESIONES.COMPANIA        = $P{PR_COMPANIA}
  AND DETALLE_PROFESIONES.ID_DE_EMPLEADO  = $P{PR_IDEMPLEADO}
  AND DETALLE_PROFESIONES.NUMERO_DCTO     = $P{PR_NUMERODCTO}
