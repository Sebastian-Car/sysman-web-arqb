SELECT COMPANIA, 
       ID_DE_PROCESO, 
       ANO,  
       MES,  
       ID_DE_EMPLEADO, 
       SUM(DEVENGOS) DEVENGOS, 
       SUM(DESCUENTOS) DESCUENTOS, 
       (SUM(DEVENGOS)- SUM(DESCUENTOS)) NETO,  
       SUM(NETOHIS) NETOHIS, 
       ROUND(SUM(DEVENGOS)-SUM(DESCUENTOS),0)-ROUND(SUM(NETOHIS),0) DIFER, 
      TO_CHAR(MIN(FECHA) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM), 'DD/MM/YYYY')  FECHA  
       FROM V_ACUMULADOS  
 WHERE COMPANIA= s$compania$s 
 AND ID_DE_PROCESO = s$proceso$s 
 AND CLASE IN (3, 5, 7) 
 AND ANO= s$ano2$s                      
 AND MES= s$mes2$s 
 GROUP BY COMPANIA, ID_DE_PROCESO, ANO, MES, ID_DE_EMPLEADO
 HAVING ROUND(SUM(DEVENGOS)-SUM(DESCUENTOS),0)-ROUND(SUM(NETOHIS),0) <> 0
