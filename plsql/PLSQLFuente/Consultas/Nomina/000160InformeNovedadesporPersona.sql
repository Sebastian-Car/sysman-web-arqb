SELECT 
  ID_DE_EMPLEADO,
  NUMERO_DCTO,
  NOMBRECOMPLETO  NOMBRECOMPLETO,
  NOMBRE_CONCEPTO,
  ID_DE_CONCEPTO,
  FECHA,
  VALOR  VAL,
  COMPANIA
FROM V_NOVEDADES
WHERE ID_DE_EMPLEADO <> 0
  AND COMPANIA = s$compania$s
 AND ID_DE_EMPLEADO = CASE WHEN s$opcion$s = 3 THEN s$empleado$s ELSE ID_DE_EMPLEADO END
  AND (LPAD(ID_DE_PROCESO,2,'0') || LPAD(ANO, 4, '0') || LPAD(MES, 2,'0') || LPAD(PERIODO,2,'0'))
  BETWEEN s$intervalo1$s 
      AND s$intervalo2$s
 ORDER BY ID_DE_EMPLEADO
