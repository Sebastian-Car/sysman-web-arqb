SELECT PERSONAL.NOMBRECOMPLETO AS NOMBRECOMPLETO,
  PERSONAL.NUMERO_DCTO,
  TIPO_EMBARGO.NOMBRE AS EMB,
  JUZGADOS.NOMBRE  AS JUZ,
  EMBARGOS.OFICIO,
  EMBARGOS.ID_DE_PROCESO,
  EMBARGOS.ANO,
  UPPER(PCK_SYSMAN_UTL.FC_NOMBRE_MES(MES))  NOMBREMES,
  EMBARGOS.PERIODO,
  EMBARGOS.ID_DE_EMPLEADO,
  EMBARGOS.FECHA_EMBARGO,
  EMBARGOS.PORCENTAJE,
  EMBARGOS.VALOR AS VR_CUOTA,
  EMBARGOS.DEMANDANTE,
  EMBARGOS.TIPO_DEMANDANTE,
  EMBARGOS.NUM_RADICACION,
  EMBARGOS.FECHA_RADICACION,
  EMBARGOS.ESTADO,
  EMBARGOS.BANCO,
  EMBARGOS.OFICINA_DESTINO,
  EMBARGOS.COMISION,
 EMBARGOS.COMISION AS IVA,
  PERSONAL.ESTADO_ACTUAL,
  BANCO.NOMBREBANCO
FROM JUZGADOS
INNER JOIN EMBARGOS
 ON JUZGADOS.COMPANIA   = EMBARGOS.COMPANIA
AND JUZGADOS.ID_JUZGADO = EMBARGOS.ID_JUZGADO
INNER JOIN TIPO_EMBARGO
 ON TIPO_EMBARGO.COMPANIA = EMBARGOS.COMPANIA
AND TIPO_EMBARGO.CODIGO   = EMBARGOS.ID_EMBARGO
INNER JOIN PERSONAL
 ON PERSONAL.COMPANIA = EMBARGOS.COMPANIA
AND PERSONAL.ID_DE_EMPLEADO      = EMBARGOS.ID_DE_EMPLEADO
LEFT JOIN BANCO 
ON EMBARGOS.COMPANIA= BANCO.COMPANIA
AND EMBARGOS.BANCO = BANCO.BANCO
WHERE JUZGADOS.COMPANIA    = s$compania$s
AND EMBARGOS.ID_DE_PROCESO = s$idProceso$s
AND EMBARGOS.ANO           = s$ano$s
AND EMBARGOS.MES           = s$mes$s
AND EMBARGOS.PERIODO       = s$periodo$s
AND EMBARGOS.ESTADO        NOT IN ('C')
AND PERSONAL.ESTADO_ACTUAL IN (1)
