SELECT 
CODIGO_ESTABLECIMIENTO ESCALAFON,
NOMBRE_ESTABLECIMIENTO NOMBREESCALAFON,
ID_DE_CONCEPTO ID_DE_CONCEPTO,
CLASE,
CLASE_NOMBRE_MAYU CLASECONCEPTO,
NOMBRE_CONCEPTO NOMBRE_CONCEPTO, 
SUM (V_ACUMULADOS.VALOR) AS SUMA_DE_VALOR
FROM V_ACUMULADOS 
WHERE V_ACUMULADOS.COMPANIA= s$compania$s
AND V_ACUMULADOS.CLASE IN (3,4,5,6,7,8)	
AND LPAD(ID_DE_PROCESO, 2, 0) || LPAD(ANO, 4, 0) || LPAD(MES, 2, 0) || LPAD(PERIODO, 2, 0) BETWEEN 's$fechaInicial$s'  AND 's$fechaFinal$s'
GROUP BY CODIGO_ESTABLECIMIENTO,
NOMBRE_ESTABLECIMIENTO ,
ID_DE_CONCEPTO,
CLASE,
CLASE_NOMBRE_MAYU ,
NOMBRE_CONCEPTO  
ORDER BY CODIGO_ESTABLECIMIENTO, CLASE, ID_DE_CONCEPTO
