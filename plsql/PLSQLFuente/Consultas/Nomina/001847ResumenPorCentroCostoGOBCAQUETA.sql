SELECT HISTORICOS.ANO,
  LOWER(PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES)) MES,
  HISTORICOS.PERIODO,
  CENTRO_COSTO.CODIGO AS ID_CENTRO_DE_COSTO,
  CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO,
  HISTORICOS.ID_DE_CONCEPTO,
  CONCEPTOS.NOMBRE_CONCEPTO,
  CONCEPTOS.CLASE,
  SUM(HISTORICOS.VALOR) AS SUMA_DE_VALOR,  
  DECODE(CONCEPTOS.CLASE, 3, 'VALOR DEVENGOS'
                        , 5, 'VALOR DESCUENTOS'
                        , 4, 'VALOR TOTAL DEVENGOS'
                        , 6, 'VALOR TOTAL DESCUENTOS'
                        , 7, 'VALOR NETO A PAGAR'
                           , 'CONCEPTOS CONTABLES') AS CLASECONCEPTO,
  PERSONAL.ID_CENTRO_DE_COSTO,
  SUM(CASE WHEN CONCEPTOS.CLASE IN ('3','8') THEN HISTORICOS.VALOR ELSE 0 END) AS VALORNOMINA
FROM PERSONAL
  INNER JOIN CENTRO_COSTO
     ON PERSONAL.COMPANIA           = CENTRO_COSTO.COMPANIA
    AND PERSONAL.ANO                = CENTRO_COSTO.ANO
    AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
  INNER JOIN HISTORICOS
     ON PERSONAL.COMPANIA       = HISTORICOS.COMPANIA
    AND PERSONAL.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
  INNER JOIN CONCEPTOS
     ON HISTORICOS.COMPANIA       = CONCEPTOS.COMPANIA
    AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
  INNER JOIN PERIODOS
     ON HISTORICOS.COMPANIA      = PERIODOS.COMPANIA
    AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO
    AND HISTORICOS.ANO           = PERIODOS.ANO
    AND HISTORICOS.MES           = PERIODOS.MES
    AND HISTORICOS.PERIODO       = PERIODOS.PERIODO
  WHERE PERSONAL.COMPANIA = s$compania$s
    AND HISTORICOS.ID_DE_PROCESO || HISTORICOS.ANO || HISTORICOS.MES || HISTORICOS.PERIODO BETWEEN 's$proceso' || 's$ano$s' || 's$mes$s' || 's$periodo$s'  AND 's$proceso$s' || 's$ano$s' || 's$mes$s' || 's$periodo$s'
    AND CONCEPTOS.CLASE IN (3,4,5,6,7,8)
GROUP BY CENTRO_COSTO.CODIGO,
  CENTRO_COSTO.NOMBRE,
  HISTORICOS.ID_DE_CONCEPTO,
  CONCEPTOS.NOMBRE_CONCEPTO,
  CONCEPTOS.CLASE,
  DECODE(CONCEPTOS.CLASE, 3, 'VALOR DEVENGOS'
                        , 5, 'VALOR DESCUENTOS'
                        , 4, 'VALOR TOTAL DEVENGOS'
                        , 6, 'VALOR TOTAL DESCUENTOS'
                        , 7, 'VALOR NETO A PAGAR'
                           , 'CONCEPTOS CONTABLES'),
  PERSONAL.ID_CENTRO_DE_COSTO,
  HISTORICOS.ID_DE_PROCESO,
HISTORICOS.ANO,
HISTORICOS.MES,
HISTORICOS.PERIODO
ORDER BY HISTORICOS.ID_DE_CONCEPTO
