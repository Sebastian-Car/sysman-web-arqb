SELECT AREAMISIONAL.CODAMISIONAL,
  AREAMISIONAL.DESCRIPCION,
  PERSONAL.APELLIDO1 || ' '|| PERSONAL.APELLIDO2 || ' '|| PERSONAL.NOMBRES FUNCIONARIO,
  DETALLEVIATICOS.NUMDIASPER NDPER,
  DETALLEVIATICOS.NUMDIASSINPER NDSINPER,
  DETALLEVIATICOS.VALDIASPER,
  DETALLEVIATICOS.VALDIASSINPER,
  NVL(DETALLEVIATICOS.TOTAL,0) TOTVIATICOS,
  PAISES.NOMBRE PAIS,
  DEPARTAMENTO.NOMBRE DEPTO,
  CIUDAD.NOMBRE CIUDADES,
  TO_CHAR(VIATICOS.FECHAINICIO,'DD/MM/YYYY') FECHAINICIO,
  TO_CHAR(VIATICOS.FECHAFIN,'DD/MM/YYYY') FECHAFIN,
  VIATICOS.OBJETO,
  NVL(CASE
	    WHEN CONCEPTOVIATICOS.TIPO_CONCEPTO IN ('T')
	    THEN TOTAL
	    ELSE 0
	   END,0) TRANSPORTE
FROM VI_DETALLE_VIATICOS DETALLEVIATICOS
LEFT JOIN VI_VIATICOS VIATICOS
	ON DETALLEVIATICOS.COMPANIA      = VIATICOS.COMPANIA
	AND DETALLEVIATICOS.ANO          = VIATICOS.ANO
	AND DETALLEVIATICOS.TIPO_VIATICO = VIATICOS.TIPO_VIATICO
	AND DETALLEVIATICOS.NUMERO       = VIATICOS.CODSOLICITUD
INNER JOIN PAISES
	ON VIATICOS.PAIS = PAISES.PAIS
INNER JOIN DEPARTAMENTO
	ON PAISES.PAIS            = DEPARTAMENTO.PAIS
	AND VIATICOS.DEPARTAMENTO = DEPARTAMENTO.CODIGO
INNER JOIN CIUDAD
	ON PAISES.PAIS          = CIUDAD.PAIS
	AND DEPARTAMENTO.CODIGO = CIUDAD.DEPARTAMENTO
	AND VIATICOS.CIUDAD     = CIUDAD.CODIGO
INNER JOIN DEPENDENCIA
	ON VIATICOS.COMPANIA  = DEPENDENCIA.COMPANIA
	AND VIATICOS.MISIONAL = DEPENDENCIA.CODIGO
INNER JOIN VI_AREAMISIONAL AREAMISIONAL
	ON DEPENDENCIA.COMPANIA      = AREAMISIONAL.COMPANIA
	AND DEPENDENCIA.CODAMISIONAL = AREAMISIONAL.CODAMISIONAL
INNER JOIN PERSONAL
	ON AREAMISIONAL.COMPANIA     = PERSONAL.COMPANIA
	AND AREAMISIONAL.FUNCIONARIO = PERSONAL.ID_DE_EMPLEADO
LEFT JOIN VI_CONCEPTO_VIATICOS CONCEPTOVIATICOS
	ON DETALLEVIATICOS.COMPANIA  = CONCEPTOVIATICOS.COMPANIA
	AND DETALLEVIATICOS.CONCEPTO = CONCEPTOVIATICOS.CODIGO_CONCEPTO
	AND DETALLEVIATICOS.ANO      = CONCEPTOVIATICOS.ANO
WHERE AREAMISIONAL.COMPANIA  = s$compania$s
  AND AREAMISIONAL.CODAMISIONAL BETWEEN 's$areainicial$s' AND 's$areafinal$s'
  AND VIATICOS.FECHAINICIO BETWEEN s$fechainicio$s AND s$fechafinal$s
  AND VIATICOS.FECHAFIN BETWEEN s$fechainicio$s AND s$fechafinal$s
ORDER BY AREAMISIONAL.CODAMISIONAL
