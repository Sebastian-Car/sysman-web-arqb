    CASE WHEN (SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 912 THEN HISTORICOS.VALOR ELSE 0 END)) > 0 THEN SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 912 THEN HISTORICOS.VALOR ELSE 0 END) - TRUNC(SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 912 THEN  HISTORICOS.VALOR ELSE 0 END)/30)*30 ELSE 0 END   LIC_DIA,                                      
    SUBSTR(PCK_SYSMAN_UTL.FC_EDAD(PERSONAL_HISTORICO.FECHA_DE_INGRESO + SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 912 THEN  HISTORICOS.VALOR ELSE 0 END), PERSONAL_HISTORICO.FECHATERCONTRATO, 1),1,2)  ANO_TSER,
    SUBSTR(PCK_SYSMAN_UTL.FC_EDAD(PERSONAL_HISTORICO.FECHA_DE_INGRESO + SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 912 THEN  HISTORICOS.VALOR ELSE 0 END), PERSONAL_HISTORICO.FECHATERCONTRATO, 1),4,2)  MES_TSERV,
    SUBSTR(PCK_SYSMAN_UTL.FC_EDAD(PERSONAL_HISTORICO.FECHA_DE_INGRESO + SUM(CASE WHEN  HISTORICOS.ID_DE_CONCEPTO = 912 THEN HISTORICOS.VALOR ELSE 0 END), PERSONAL_HISTORICO.FECHATERCONTRATO, 1),7,2)  DIA_TSERV,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 973 THEN HISTORICOS.VALOR ELSE 0 END) DIASDURACIONCONTRATO, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 009 THEN HISTORICOS.VALOR ELSE 0 END) DIASC,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 969 THEN HISTORICOS.VALOR ELSE 0 END) PROMCES,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 911 THEN HISTORICOS.VALOR ELSE 0 END) ANTCES,
    CASE WHEN SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 160 THEN HISTORICOS.VALOR ELSE 0 END) > 0 THEN SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 985 THEN HISTORICOS.VALOR ELSE 0 END) ELSE 0 END C160,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 985 THEN HISTORICOS.VALOR ELSE 0 END) C985,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 987 THEN HISTORICOS.VALOR ELSE 0 END) C987,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 988 THEN HISTORICOS.VALOR ELSE 0 END) C988,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 067 THEN HISTORICOS.VALOR ELSE 0 END) C067,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 096 THEN HISTORICOS.VALOR ELSE 0 END) C096,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 112 THEN HISTORICOS.VALOR ELSE 0 END) C112,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 140 THEN HISTORICOS.VALOR ELSE 0 END) C140,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 158 THEN HISTORICOS.VALOR ELSE 0 END) PRIMANAV,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 160 THEN HISTORICOS.VALOR ELSE 0 END) PRIMAJUN,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 120 THEN HISTORICOS.VALOR ELSE 0 END) FSPAD,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 125 THEN HISTORICOS.VALOR ELSE 0 END) RTEFTE,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 130 THEN HISTORICOS.VALOR ELSE 0 END) SALUD,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 131 THEN HISTORICOS.VALOR ELSE 0 END) PENSION, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 132 THEN HISTORICOS.VALOR ELSE 0 END) FSP, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 140 THEN HISTORICOS.VALOR ELSE 0 END) DESCUENTOS,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 144 THEN HISTORICOS.VALOR ELSE 0 END) NETO,
    CASE WHEN SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 159 THEN HISTORICOS.VALOR ELSE 0 END)>0 THEN SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 986 THEN HISTORICOS.VALOR ELSE 0 END) ELSE 0 END  C159,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 169 THEN HISTORICOS.VALOR ELSE 0 END) INTCES,  
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 175 THEN HISTORICOS.VALOR ELSE 0 END) + SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 176 THEN HISTORICOS.VALOR ELSE 0 END) AS VACDINERO,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 177 THEN HISTORICOS.VALOR ELSE 0 END) CES,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 600 THEN HISTORICOS.VALOR ELSE 0 END) C600,
    PCK_NOMINA_COM2.FC_NOMBRECONCEPTO('001',600) NOMC600,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 601 THEN  HISTORICOS.VALOR ELSE 0  END) C601,
    PCK_NOMINA_COM2.FC_NOMBRECONCEPTO('001',601) NOMC601,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 602 THEN  HISTORICOS.VALOR ELSE 0  END) C602,
    PCK_NOMINA_COM2.FC_NOMBRECONCEPTO('001',602) NOMC602,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 603 THEN  HISTORICOS.VALOR ELSE 0  END) C603,
    PCK_NOMINA_COM2.FC_NOMBRECONCEPTO('001',603) NOMC603,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 604 THEN  HISTORICOS.VALOR ELSE 0  END) C604,
    PCK_NOMINA_COM2.FC_NOMBRECONCEPTO('001',604) NOMC604,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 605 THEN  HISTORICOS.VALOR ELSE 0  END) C605,
    PCK_NOMINA_COM2.FC_NOMBRECONCEPTO('001',605) NOMC605,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 606 THEN  HISTORICOS.VALOR ELSE 0  END) C606,
    PCK_NOMINA_COM2.FC_NOMBRECONCEPTO('001',606) NOMC606,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 607 THEN  HISTORICOS.VALOR ELSE 0  END) C607,
    PCK_NOMINA_COM2.FC_NOMBRECONCEPTO('001',607) NOMC607,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 826 THEN  HISTORICOS.VALOR ELSE 0  END) C826,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 902 THEN  HISTORICOS.VALOR ELSE 0  END) EXTRAS, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 903 THEN  HISTORICOS.VALOR ELSE 0  END) C903,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 952 THEN  HISTORICOS.VALOR ELSE 0  END) C952,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 964 THEN  HISTORICOS.VALOR ELSE 0  END) C964,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 969 THEN  HISTORICOS.VALOR ELSE 0  END) SALARIO_PROMEDIO_MENSUAL,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 986 THEN  HISTORICOS.VALOR ELSE 0  END) C986        
FROM PERSONAL_HISTORICO INNER JOIN HISTORICOS ON PERSONAL_HISTORICO.COMPANIA = HISTORICOS.COMPANIA
             AND PERSONAL_HISTORICO.ID_DE_PROCESO = HISTORICOS.ID_DE_PROCESO
             AND PERSONAL_HISTORICO.ANO = HISTORICOS.ANO
             AND PERSONAL_HISTORICO.MES = HISTORICOS.MES
             AND PERSONAL_HISTORICO.PERIODO = HISTORICOS.PERIODO
             AND PERSONAL_HISTORICO.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
 LEFT JOIN CAUSARETIRO ON PERSONAL_HISTORICO.COMPANIA = CAUSARETIRO.COMPANIA
                   AND PERSONAL_HISTORICO.CAUSA_RETIRO = CAUSARETIRO.CODIGO
WHERE PERSONAL_HISTORICO.COMPANIA = s$compania$s
AND PERSONAL_HISTORICO.ID_DE_PROCESO = s$idProceso$s
AND PERSONAL_HISTORICO.ANO = s$ano$s
AND PERSONAL_HISTORICO.MES = s$mes$s
AND PERSONAL_HISTORICO.PERIODO = s$periodo$s
AND PERSONAL_HISTORICO.ID_DE_EMPLEADO = s$idEmpleado$s 
GROUP BY PERSONAL_HISTORICO.COMPANIA,
    PERSONAL_HISTORICO.ID_DE_PROCESO,
    PERSONAL_HISTORICO.ANO,
    PERSONAL_HISTORICO.MES,
    PERSONAL_HISTORICO.PERIODO,
    PERSONAL_HISTORICO.NOMBRECOMPLETO, 
    PERSONAL_HISTORICO.NUMERO_DCTO, 
    PERSONAL_HISTORICO.NOMBRE_DE_CARGO,
    PERSONAL_HISTORICO.NOMBRE_FORMANOMBRAMIENTO,
    PERSONAL_HISTORICO.ID_DE_EMPLEADO,
    CAUSARETIRO.NOMBRE ,
    PERSONAL_HISTORICO.FECHATERCONTRATO,
    PERSONAL_HISTORICO.FECHA_DE_INGRESO,
    PERSONAL_HISTORICO.NOMBRE_FONDOCESANTIAS,
    PERSONAL_HISTORICO.NOMBRE_FONDOPENSION,
    PERSONAL_HISTORICO.NOMBRE_FONDOSALUD,
    PERSONAL_HISTORICO.NOMBRE_CENTRO_DE_COSTO,
    CASE WHEN PERSONAL_HISTORICO.REGIMEN =1 THEN 'Retroactivas' ELSE CASE WHEN PERSONAL_HISTORICO.REGIMEN=2 THEN 'Ley 50/1990'ELSE 'Salario Integral' END END,
    PCK_NOMINA_COM2.FC_NOMBRECONCEPTO('001',600),
    PCK_NOMINA_COM2.FC_NOMBRECONCEPTO('001',601),
    PCK_NOMINA_COM2.FC_NOMBRECONCEPTO('001',602),
    PCK_NOMINA_COM2.FC_NOMBRECONCEPTO('001',603),
    PCK_NOMINA_COM2.FC_NOMBRECONCEPTO('001',604),
    PCK_NOMINA_COM2.FC_NOMBRECONCEPTO('001',605),
    PCK_NOMINA_COM2.FC_NOMBRECONCEPTO('001',606),
    PCK_NOMINA_COM2.FC_NOMBRECONCEPTO('001',607)
ORDER BY 
    PERSONAL_HISTORICO.NOMBRECOMPLETO