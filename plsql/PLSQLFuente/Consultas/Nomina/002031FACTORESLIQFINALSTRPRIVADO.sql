    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 036 THEN HISTORICOS.VALOR ELSE 0 END) C036,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 079 THEN HISTORICOS.VALOR ELSE 0 END) AUXALIMENTACION, 
