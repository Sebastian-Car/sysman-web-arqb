SELECT HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.COMPANIA,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  HISTORICOS.ID_DE_EMPLEADO,
  HISTORICOS.ID_DE_CONCEPTO,
  HISTORICOS.VALOR APORTE,
  CONCEPTOS.NOMBRE_CONCEPTO NOMBRE_CONCEPTO,
  PERSONAL.NOMBRECOMPLETO NOMCOMPLETO,
  PERSONAL.NUMERO_DCTO,
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO IN ('127')
    THEN V_FONDO_DE_PENSION_AFC.NOMBRE_FONDO_AFC
    ELSE
      CASE
        WHEN HISTORICOS.ID_DE_CONCEPTO IN ('100')
        THEN V_MEDICINA_PREPAGADA.NOMBRE_MEDICINA
        ELSE
          CASE
            WHEN HISTORICOS.ID_DE_CONCEPTO IN ('124')
            THEN V_FONDO_DE_PENSION_VOL.NOMBRE_DEL_FONDO
            ELSE
              CASE
                WHEN HISTORICOS.ID_DE_CONCEPTO IN ('125')
                THEN 'DIRECCION DE IMPUESTOS Y ADUANAS NACIONALES'
                ELSE CONCEPTOS.NOMBRE_CONCEPTO
              END
          END
      END
  END NOMBREFONDO,
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO IN ('127')
    THEN V_FONDO_DE_PENSION_AFC.NIT
    ELSE
      CASE
        WHEN HISTORICOS.ID_DE_CONCEPTO IN ('100')
        THEN V_MEDICINA_PREPAGADA.NIT
        ELSE
          CASE
            WHEN HISTORICOS.ID_DE_CONCEPTO IN ('124')
            THEN V_FONDO_DE_PENSION_VOL.NIT
            ELSE
              CASE
                WHEN HISTORICOS.ID_DE_CONCEPTO IN ('130')
                THEN V_FONDO_DE_SALUD.NIT
                ELSE
                  CASE
                    WHEN HISTORICOS.ID_DE_CONCEPTO IN ('131')
                    THEN V_FONDO_DE_PENSIONES.NIT
                    ELSE CONCEPTOS.TERCERO
                  END
              END
          END
      END
  END NITFONDO
FROM HISTORICOS
  INNER JOIN CONCEPTOS
    ON HISTORICOS.COMPANIA        = CONCEPTOS.COMPANIA
    AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
  INNER JOIN PERSONAL
    ON HISTORICOS.COMPANIA        = PERSONAL.COMPANIA
    AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
  LEFT JOIN V_MEDICINA_PREPAGADA
    ON PERSONAL.COMPANIA            = V_MEDICINA_PREPAGADA.COMPANIA
    AND PERSONAL.MEDICINA_PREPAGADA = V_MEDICINA_PREPAGADA.MEDICINA_PREPAGADA
  LEFT JOIN V_FONDO_DE_PENSION_AFC
    ON PERSONAL.COMPANIA   = V_FONDO_DE_PENSION_AFC.COMPANIA
    AND PERSONAL.FONDO_AFC = V_FONDO_DE_PENSION_AFC.FONDO_AFC
  LEFT JOIN V_FONDO_DE_PENSION_VOL
    ON PERSONAL.COMPANIA           = V_FONDO_DE_PENSION_VOL.COMPANIA
    AND PERSONAL.FONDO_PENSION_VOL = V_FONDO_DE_PENSION_VOL.ID_DEL_FONDO
  LEFT JOIN V_FONDO_DE_SALUD
    ON PERSONAL.COMPANIA     = V_FONDO_DE_SALUD.COMPANIA
    AND PERSONAL.FONDO_SALUD = V_FONDO_DE_SALUD.FONDO_SALUD
  LEFT JOIN V_FONDO_DE_PENSIONES
    ON PERSONAL.COMPANIA               = V_FONDO_DE_PENSIONES.COMPANIA
    AND PERSONAL.ID_DEL_FONDO          = V_FONDO_DE_PENSIONES.ID_DEL_FONDO
WHERE HISTORICOS.COMPANIA          = s$compania$s
AND HISTORICOS.ANO                 = s$ano1$s
AND HISTORICOS.MES                 = s$mes1$s
AND HISTORICOS.PERIODO             = s$periodo1$s
AND HISTORICOS.ID_DE_CONCEPTO NOT IN (120,130,131,132,134)
AND CONCEPTOS.CLASE               IN ('5')
