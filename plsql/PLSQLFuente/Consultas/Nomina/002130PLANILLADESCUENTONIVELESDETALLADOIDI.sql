SELECT * 
FROM (
	SELECT 
		HISTORICOS.ID_DE_EMPLEADO AS CODIGO, 
		PERSONAL.ID_DEL_FONDO AS FP, 
		CARGOS.ESCALAFON, 
		PERSONAL.FONDO_SALUD AS FS,
		SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '079' THEN HISTORICOS.VALOR ELSE 0 END + CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '080' THEN HISTORICOS.VALOR ELSE 0 END) AS TRA, 
		SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '174' THEN HISTORICOS.VALOR ELSE 0 END + CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '175' THEN HISTORICOS.VALOR ELSE 0 END) AS VAC,  
		Sum(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '151' THEN HISTORICOS.VALOR ELSE 0 END + CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '381' THEN HISTORICOS.VALOR ELSE 0 END + 
			CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '207' THEN HISTORICOS.VALOR ELSE 0 END + CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '169' THEN HISTORICOS.VALOR ELSE 0 END + 
			CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '177' THEN HISTORICOS.VALOR ELSE 0 END + CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '532' THEN HISTORICOS.VALOR ELSE 0 END + 
			CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '533' THEN HISTORICOS.VALOR ELSE 0 END + CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '202' THEN HISTORICOS.VALOR ELSE 0 END ) AS Ajust,
		
		SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '061' THEN HISTORICOS.VALOR ELSE 0 END + CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '183' THEN HISTORICOS.VALOR ELSE 0 END + 
			CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '170' THEN HISTORICOS.VALOR ELSE 0 END + CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '171' THEN HISTORICOS.VALOR ELSE 0 END) AS TRAA, 
		SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '155' THEN HISTORICOS.VALOR ELSE 0 END + CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '150' THEN HISTORICOS.VALOR ELSE 0 END + 
			CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '180' THEN HISTORICOS.VALOR ELSE 0 END) AS V, 
		MAX(HISTORICOS.ID_DE_PROCESO) AS PROCESO, 
		MAX(HISTORICOS.ANO) AS ANO, 
		MAX(HISTORICOS.MES) AS MES, 
		MAX(PERSONAL.ID_DE_CARGO) AS CARGO, 
		MAX(CARGOS.NOMBRE_DEL_CARGO) AS NOMBRECARGO, 
		MAX(PERSONAL.ID_CENTRO_DE_COSTO) AS Centro_Costo, 
       	MAX(CENTRO_COSTO.NOMBRE) AS NombreCentro, 
		MAX(PERSONAL.DEPENDENCIA) AS DEPENDENCIA,
		MAX(DEPENDENCIA.NOMBRE) AS NOMBREDEPENDENCIA, 
		MAX(HISTORICOS.PERIODO) AS PERIODO, 
		MAX(PERSONAL.APELLIDO1 || ' ' || PERSONAL.APELLIDO2 || ' ' || PERSONAL.NOMBRES) AS NOM_EMPLEADO, 
		MAX(PERSONAL.NUMERO_DCTO) AS CEDULA, 
		SUM(CASE WHEN CONCEPTOS.CLASE=3 THEN HISTORICOS.VALOR ELSE 0 END) AS DEVENGOS, 
		SUM(CASE WHEN CONCEPTOS.CLASE=5 THEN HISTORICOS.VALOR ELSE 0 END) AS DESCUENTOS, 
		SUM(CASE WHEN CONCEPTOS.CLASE=3 THEN HISTORICOS.VALOR ELSE 0 END) - SUM(CASE WHEN CONCEPTOS.CLASE=5 THEN HISTORICOS.VALOR ELSE 0 END) AS NETO,
    	'C' || HISTORICOS.ID_DE_CONCEPTO as CONCEPTO,
    	HISTORICOS.VALOR AS TOTAL
	FROM 
		HISTORICOS
		INNER JOIN PERSONAL
			ON HISTORICOS.COMPANIA = PERSONAL.COMPANIA
			AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
		INNER JOIN CONCEPTOS
			ON HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA
			AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
		INNER JOIN PERIODOS
			ON HISTORICOS.COMPANIA = PERIODOS.COMPANIA
			AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO
			AND HISTORICOS.ANO = PERIODOS.ANO
			AND HISTORICOS.MES = PERIODOS.MES
			AND HISTORICOS.PERIODO = PERIODOS.PERIODO
		INNER JOIN CARGOS
			ON PERSONAL.COMPANIA = CARGOS.COMPANIA
			AND PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO
		INNER JOIN CENTRO_COSTO 
          ON PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
          AND PERSONAL.COMPANIA = CENTRO_COSTO.COMPANIA
          AND PERSONAL.ANO = CENTRO_COSTO.ANO
		INNER JOIN DEPENDENCIA
			ON PERSONAL.COMPANIA = DEPENDENCIA.COMPANIA
			AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
	WHERE 
		PERSONAL.ESTADO_ACTUAL <> 0  
		AND PERIODOS.ACUMULADO <> 0 
		AND HISTORICOS.ID_DE_PROCESO = s$proceso$s
		AND HISTORICOS.ANO = s$ano$s
		AND HISTORICOS.MES = s$mes$s  
		AND HISTORICOS.PERIODO = s$periodo$s
	GROUP BY
		HISTORICOS.ID_DE_EMPLEADO, 
		PERSONAL.ID_DEL_FONDO, 
		CARGOS.ESCALAFON,
		PERSONAL.FONDO_SALUD,
	    'C' || HISTORICOS.ID_DE_CONCEPTO,
	    HISTORICOS.VALOR
	)
PIVOT (
  SUM(TOTAL)
  FOR CONCEPTO IN ('C001' AS "C001", 'C002' AS "C002",
  		'C009' AS "C009",'C061' AS "C061",'C079' AS "C079", 'C080' AS "C080", 
  		'C097' AS "C097",'C112' AS "C112",'C113' AS "C113",'C115' AS "C115",
  		'C118' AS "C118",'C125' AS "C125",'C140' AS "C140",'C144' AS "C144",'C145' AS "C145",
  		'C150' AS "C150",'C155' AS "C155",'C170' AS "C170",'C171' AS "C171",'C174' AS "C174",'C175' AS "C175",'C180' AS "C180",
  		'C183' AS "C183",'C250' AS "C250",'C378' AS "C378",'C379' AS "C379",
  		'C699' AS "C699",'C799' AS "C799")
)