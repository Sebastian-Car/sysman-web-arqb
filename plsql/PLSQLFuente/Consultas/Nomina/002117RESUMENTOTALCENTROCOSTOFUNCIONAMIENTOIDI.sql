SELECT  
	HISTORICOS.ID_DE_CONCEPTO, 
	CONCEPTOS.NOMBRE_CONCEPTO, 
	CONCEPTOS.CLASE, 
	SUM(HISTORICOS.VALOR) AS SUMA_DE_VALOR, 
	CASE WHEN CONCEPTOS.CLASE=3 THEN 'VALOR DEVENGOS' ELSE 
		CASE WHEN CONCEPTOS.CLASE=5 THEN 'VALOR DESCUENTOS' ELSE
			CASE WHEN CONCEPTOS.CLASE=7 THEN 'NETO PAGAR' ELSE 
				CASE WHEN CONCEPTOS.CLASE=4 THEN 'TOTAL DEVENGADO' ELSE
					CASE WHEN CONCEPTOS.CLASE=6 THEN 'TOTAL DEDUCCIONES' ELSE
						CASE WHEN CONCEPTOS.CLASE=9 THEN 'TOTALCUENTAS POR COBRAR' ELSE 
							'APORTES PATRONALES' 
						END
					END
				END
			END
		END
	END AS CLASECONCEPTO, 
	PERSONAL.ESTADO_ACTUAL, 
	CENTRO_COSTO.CODIGO, 
	CENTRO_COSTO.NOMBRE,
	PERSONAL.DE_CARRERA
FROM 
	HISTORICOS
	INNER JOIN PERSONAL
		ON HISTORICOS.COMPANIA = PERSONAL.COMPANIA
		AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
	INNER JOIN CONCEPTOS
		ON HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA
		AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
	INNER JOIN PERIODOS
		ON HISTORICOS.COMPANIA = PERIODOS.COMPANIA
		AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO
		AND HISTORICOS.ANO = PERIODOS.ANO
		AND HISTORICOS.MES = PERIODOS.MES
		AND HISTORICOS.PERIODO = PERIODOS.PERIODO
	INNER JOIN CENTRO_COSTO 
      ON PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
      AND PERSONAL.COMPANIA = CENTRO_COSTO.COMPANIA
      AND PERSONAL.ANO = CENTRO_COSTO.ANO
WHERE 
	HISTORICOS.ID_DE_PROCESO = s$proceso$s
	AND HISTORICOS.ANO BETWEEN s$ano1$s AND s$anio2$s
	AND HISTORICOS.MES BETWEEN s$mes1$s AND s$mes2$s
	AND HISTORICOS.PERIODO BETWEEN s$periodo1$s AND s$periodo2$s
	AND (CONCEPTOS.CLASE = 8 
		OR CONCEPTOS.CLASE = 3 
		OR CONCEPTOS.CLASE = 5 
		OR CONCEPTOS.CLASE = 7 
		OR CONCEPTOS.CLASE = 4 
		OR CONCEPTOS.CLASE = 6 
		OR CONCEPTOS.CLASE = 9)
	AND PERSONAL.ESTADO_ACTUAL = 1
	AND (CENTRO_COSTO.CODIGO = '31102' 
		OR CENTRO_COSTO.CODIGO = '31101')
GROUP BY
	HISTORICOS.ID_DE_CONCEPTO, 
	CONCEPTOS.NOMBRE_CONCEPTO, 
	CONCEPTOS.CLASE, 
	PERSONAL.ESTADO_ACTUAL, 
	CENTRO_COSTO.CODIGO, 
	CENTRO_COSTO.NOMBRE, 
	PERSONAL.DE_CARRERA
ORDER BY HISTORICOS.ID_DE_CONCEPTO