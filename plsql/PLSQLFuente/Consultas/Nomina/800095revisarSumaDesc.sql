SELECT HISTORICOS.COMPANIA,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  HISTORICOS.ID_DE_CONCEPTO,
  SUM(HISTORICOS.VALOR) AS TOTAL,
  CATEGORIA.SALARIO_BASE,
  ROUND(CATEGORIA.SALARIO_BASE /2,0)                     AS "50%_TOPEMAXIMO",
  ROUND(CATEGORIA.SALARIO_BASE/2,0)-HISTORICOS.VALOR AS MAYOR,
  PERSONAL.ID_DE_EMPLEADO,
  PERSONAL.APELLIDO1,
  PERSONAL.APELLIDO2,
  PERSONAL.NOMBRES
FROM HISTORICOS
LEFT JOIN PERSONAL
ON HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
AND HISTORICOS.COMPANIA      = PERSONAL.COMPANIA
LEFT JOIN CATEGORIA
ON PERSONAL.ANO              = CATEGORIA.ANO
AND PERSONAL.ID_DE_CATEGORIA = CATEGORIA.ID_DE_CATEGORIA
AND PERSONAL.ESCALAFON       = CATEGORIA.ESCALAFON
AND PERSONAL.COMPANIA        = CATEGORIA.COMPANIA
WHERE HISTORICOS.COMPANIA = s$compania$s
AND HISTORICOS.ID_DE_CONCEPTO=  '140'
AND HISTORICOS.ID_DE_PROCESO = s$procesoSesion$s
AND HISTORICOS.ANO           =s$anioSesion$s
AND HISTORICOS.PERIODO = s$periodoSesion$s
AND HISTORICOS.MES = s$mesSesion$s
GROUP BY HISTORICOS.COMPANIA,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  HISTORICOS.ID_DE_CONCEPTO,
  CATEGORIA.SALARIO_BASE,
  ROUND(CATEGORIA.SALARIO_BASE/2,0)-HISTORICOS.VALOR,
  PERSONAL.ID_DE_EMPLEADO,
  PERSONAL.APELLIDO1,
  PERSONAL.APELLIDO2,
  PERSONAL.NOMBRES
HAVING ROUND(CATEGORIA.SALARIO_BASE   /2,0)-HISTORICOS.VALOR<0
ORDER BY  ROUND(CATEGORIA.SALARIO_BASE   /2,0)-HISTORICOS.VALOR
