  WITH DESTINO AS
    (SELECT PAISES.NOMBRE NOMBREPAIS,
      DEPARTAMENTO.NOMBRE NOMBREDEPARTAMENTO,
      CIUDAD.NOMBRE NOMBRECIUDAD,
      PAISES.PAIS,
      DEPARTAMENTO.CODIGO DEPARTAMENTO,
      CIUDAD.CODIGO CIUDAD
    FROM PAISES
    INNER JOIN DEPARTAMENTO
    ON DEPARTAMENTO.PAIS = PAISES.PAIS
    INNER JOIN CIUDAD
    ON CIUDAD.PAIS          = PAISES.PAIS
    AND CIUDAD.DEPARTAMENTO = DEPARTAMENTO.CODIGO
    )
  SELECT DEPENDENCIA.CODIGO DEPENDENCIA,
    DEPENDENCIA.NOMBRE AREA,
    VI_VIATICOS.FECHAINICIO,
    VI_VIATICOS.FECHAFIN,
    VI_VIATICOS.OBJETO,
    NVL(DESTINO.NOMBREPAIS, ' ') PAIS,
    NVL(DESTINO.NOMBREDEPARTAMENTO, ' ') DEPTO,
    NVL(DESTINO.NOMBRECIUDAD, ' ') CIUDADES,
    UPPER(PERSONAL.APELLIDO1
    || ' '
    || TRIM(PERSONAL.APELLIDO2)
    || ' '
    || TRIM(PERSONAL.NOMBRES)) FUNCIONARIO,
    VI_VIATICOS.VALORTOTALVIATICO TOTVIATICOS,
    VI_VIATICOS.NODIAS NDPER,
    VI_VIATICOS.VALOR_DIARIO_COMISION VALDIASPER,
    VI_VIATICOS.VALORTOTALVIATICO   TRANSPORTE
  FROM VI_VIATICOS
  INNER JOIN DEPENDENCIA
  ON VI_VIATICOS.COMPANIA     = DEPENDENCIA.COMPANIA
  AND VI_VIATICOS.DEPENDENCIA = DEPENDENCIA.CODIGO
  INNER JOIN DESTINO
  ON VI_VIATICOS.CIUDAD_DESTINO        = DESTINO.CIUDAD
  AND VI_VIATICOS.DEPARTAMENTO_DESTINO = DESTINO.DEPARTAMENTO
  AND VI_VIATICOS.PAIS_DESTINO         = DESTINO.PAIS
  INNER JOIN TERCERO
  ON VI_VIATICOS.COMPANIA = TERCERO.COMPANIA
  AND VI_VIATICOS.TERCERO = TERCERO.NIT
  INNER JOIN PERSONAL
  ON TERCERO.COMPANIA        = PERSONAL.COMPANIA
  AND TERCERO.NIT            = PERSONAL.NUMERO_DCTO
  AND TERCERO.SUCURSAL       = PERSONAL.SUCURSAL
  WHERE VI_VIATICOS.COMPANIA = s$compania$s
  AND VI_VIATICOS.DEPENDENCIA BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
  AND VI_VIATICOS.FECHAINICIO BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  GROUP BY DEPENDENCIA.CODIGO,
    DEPENDENCIA.NOMBRE,
    UPPER(PERSONAL.APELLIDO1
    || ' '
    || TRIM(PERSONAL.APELLIDO2)
    || ' '
    || TRIM(PERSONAL.NOMBRES)),
    NVL(DESTINO.NOMBREPAIS, ' '),
    NVL(DESTINO.NOMBREDEPARTAMENTO, ' '),
    NVL(DESTINO.NOMBRECIUDAD, ' '),
    VI_VIATICOS.FECHAINICIO,
    VI_VIATICOS.FECHAFIN,
    VI_VIATICOS.OBJETO,
    VI_VIATICOS.VALORTOTALVIATICO,
    VI_VIATICOS.NODIAS,
    VI_VIATICOS.VALOR_DIARIO_COMISION,
    VI_VIATICOS.SALDOTOTAL
