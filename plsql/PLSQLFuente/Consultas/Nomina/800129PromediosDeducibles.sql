SELECT 
  PERSONAL.APELLIDO1,
  PERSONAL.APELLIDO2,
  PERSONAL.NOMBRES,
  PERSONAL.ESTADO_ACTUAL,
  R.ID_DE_EMPLEADO,
  SUM(R.DED_2_1_VIIVENDA)       AS VIVIENDA,
  SUM(R.DED_2_2_MEDICINA)       AS MEDICINA,
  SUM(R.DED_2_3_PERSONASACARGO) AS PERSONASACARGO,
  SUM(R.DED_2_4_PROMEDIOSALUD)  AS PROMEDIOSALUD
FROM RETEFUENTE_CALCULOS R
  INNER JOIN PERSONAL
    ON R.COMPANIA        = PERSONAL.COMPANIA
   AND R.ID_DE_EMPLEADO  = PERSONAL.ID_DE_EMPLEADO
WHERE R.COMPANIA        = s$compania$s 
  AND R.ANO||R.MES      BETWEEN  s$ano1$s||s$mes1$s AND  s$ano2$s
||s$mes2$s
  AND R.PERIODO         =  CASE WHEN s$parNomina$s NOT IN(0) THEN 3 ELSE 2 END
  AND PERSONAL.ESTADO_ACTUAL IN (1)
GROUP BY PERSONAL.APELLIDO1,  
  PERSONAL.APELLIDO2,
  PERSONAL.NOMBRES,  
  PERSONAL.ESTADO_ACTUAL,  
  R.ID_DE_EMPLEADO
ORDER BY PERSONAL.APELLIDO1,  
  PERSONAL.APELLIDO2,  
  PERSONAL.NOMBRES
