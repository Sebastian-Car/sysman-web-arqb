SELECT PERSONAL.ID_DE_EMPLEADO,
 TRIM(APELLIDO1) || ' ' || TRIM(APELLIDO2) || ' ' || TRIM(NOMBRES) AS NOMCOMPLETO,
  PERSONAL.FONDO_CESANTIAS,
  PERSONAL.NUMERO_DCTO,
  CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO,
  DEPENDENCIA.NOMBRE,
  CARGOS.NOMBRE_DEL_CARGO,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  PCK_SYSMAN_UTL.FC_NOMBRE_MES(MES) AS MES,
  HISTORICOS.PERIODO,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='821' THEN HISTORICOS.VALOR ELSE 0 END) AS SUELDO,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='827' THEN HISTORICOS.VALOR ELSE 0 END) AS ANTIGUEDAD,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='825' THEN HISTORICOS.VALOR ELSE 0 END) AS GASTOS,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='823' THEN HISTORICOS.VALOR ELSE 0 END) AS TRANS, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='824' THEN HISTORICOS.VALOR ELSE 0 END) AS ALIM,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='832' THEN HISTORICOS.VALOR ELSE 0 END)*12+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='928' THEN HISTORICOS.VALOR ELSE 0 END)*12 AS PRIMAVAC,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='830' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMATEC,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='831' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='927' THEN HISTORICOS.VALOR ELSE 0 END) AS BONIF,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='835' THEN HISTORICOS.VALOR ELSE 0 END) AS SUELDOBASE,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='813' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMACLI,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='834' THEN HISTORICOS.VALOR ELSE 0 END) AS QUINQUENIO,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='833' THEN HISTORICOS.VALOR ELSE 0 END) AS DIASPS,							
 ROUND(SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='832' THEN HISTORICOS.VALOR ELSE 0 END)*12+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='928' THEN HISTORICOS.VALOR ELSE 0 END)*12/12,0) AS DOCPVAC,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='834' THEN HISTORICOS.VALOR ELSE 0 END)/12 AS DOCQUIN,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='832' THEN HISTORICOS.VALOR ELSE 0 END)*12+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='928' THEN HISTORICOS.VALOR ELSE 0 END)*12/12 AS DOCBON,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='833' THEN 
 HISTORICOS.VALOR ELSE 0 END) AS TOTDIAS,
MAX(TO_CHAR(HISTORICOS.FECHA,'DD/MM/YYYY')) AS FECHALIQ, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='347' THEN
 HISTORICOS.VALOR ELSE 0 END) AS DIASLICENC, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='835' THEN 
 HISTORICOS.VALOR ELSE 0 END) AS PRIMASEMEST,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='503' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='160' THEN HISTORICOS.VALOR ELSE 0 END) AS SEMESTPROPORC
FROM HISTORICOS 
INNER JOIN PERSONAL 
ON PERSONAL.COMPANIA=HISTORICOS.COMPANIA
AND PERSONAL.ID_DE_EMPLEADO=HISTORICOS.ID_DE_EMPLEADO
INNER JOIN DEPENDENCIA
ON DEPENDENCIA.CODIGO = PERSONAL.DEPENDENCIA
AND DEPENDENCIA.COMPANIA=PERSONAL.COMPANIA
INNER JOIN CENTRO_COSTO 
ON CENTRO_COSTO.CODIGO= PERSONAL.ID_CENTRO_DE_COSTO 
AND CENTRO_COSTO.COMPANIA=PERSONAL.COMPANIA
AND CENTRO_COSTO.ANO=HISTORICOS.ANO
INNER JOIN CARGOS
ON CARGOS.ID_DE_CARGO = PERSONAL.ID_DE_CARGO 
AND CARGOS.COMPANIA=PERSONAL.COMPANIA
WHERE 
(HISTORICOS.ID_DE_CONCEPTO ='160' 
OR HISTORICOS.ID_DE_CONCEPTO ='821' 
OR HISTORICOS.ID_DE_CONCEPTO ='827' 
OR HISTORICOS.ID_DE_CONCEPTO ='823' 
OR HISTORICOS.ID_DE_CONCEPTO ='824' 
OR HISTORICOS.ID_DE_CONCEPTO ='825' 
OR HISTORICOS.ID_DE_CONCEPTO ='830' 
OR HISTORICOS.ID_DE_CONCEPTO ='832' 
OR HISTORICOS.ID_DE_CONCEPTO ='834' 
OR HISTORICOS.ID_DE_CONCEPTO ='833' 
OR HISTORICOS.ID_DE_CONCEPTO ='831' 
OR HISTORICOS.ID_DE_CONCEPTO ='826' 
OR HISTORICOS.ID_DE_CONCEPTO ='835' 
OR HISTORICOS.ID_DE_CONCEPTO ='813' 
OR HISTORICOS.ID_DE_CONCEPTO='826' 
OR HISTORICOS.ID_DE_CONCEPTO='038' 
OR HISTORICOS.ID_DE_CONCEPTO='503' 
OR HISTORICOS.ID_DE_CONCEPTO='927' 
OR HISTORICOS.ID_DE_CONCEPTO='928'
)
AND HISTORICOS.ANO=s$ano$s
AND HISTORICOS.MES=s$mes$s
AND HISTORICOS.PERIODO=s$periodo$s
AND HISTORICOS.ID_DE_PROCESO=s$idProceso$s
GROUP BY PERSONAL.ID_DE_EMPLEADO,
TRIM(APELLIDO1) || ' ' || TRIM(APELLIDO2) || ' ' || TRIM(NOMBRES),
PERSONAL.FONDO_CESANTIAS,
PERSONAL.NUMERO_DCTO,
CENTRO_COSTO.NOMBRE,
DEPENDENCIA.NOMBRE,
CARGOS.NOMBRE_DEL_CARGO,
HISTORICOS.ID_DE_PROCESO,
HISTORICOS.ANO,
HISTORICOS.MES,
HISTORICOS.PERIODO,
PERSONAL.ID_CENTRO_DE_COSTO
HAVING 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='503' THEN HISTORICOS.VALOR ELSE 0 END)+SUM (CASE WHEN HISTORICOS.ID_DE_CONCEPTO='160' THEN HISTORICOS.VALOR ELSE 0 END)<>0
ORDER BY TRIM(APELLIDO1) || ' ' || TRIM(APELLIDO2) || ' ' || TRIM(NOMBRES)