SELECT PERSONAL.NOMBRECOMPLETO NOM_COMPLETO, 
      INITCAP(TO_CHAR(TO_DATE(TO_CHAR(FECHANCTO,'DD/MM/')||TO_CHAR(SYSDATE,'YYYY')),'DAY', 'NLS_DATE_LANGUAGE=SPANISH')) NOMDIA,
      INITCAP(TO_CHAR(TO_DATE(TO_CHAR(FECHANCTO,'DD/MM/')||TO_CHAR(SYSDATE,'YYYY')),'DAY')) DIAT,
      TO_CHAR(FECHANCTO,'DD') DIA, 
      PCK_SYSMAN_UTL.FC_NOMBRE_MES(TO_CHAR(FECHANCTO,'MM')) NOMBREM,  
      TO_CHAR(FECHANCTO,'MM')  MES, 
      PERSONAL.ID_DE_EMPLEADO, 
      PERSONAL.COMPANIA, 
      PERSONAL.ESTADO_ACTUAL, 
      PERSONAL.FECHANCTO 
FROM PERSONAL 
WHERE PERSONAL.COMPANIA          =  s$compania$s
  AND PERSONAL.ID_DE_EMPLEADO    <> 0 
  AND PERSONAL.ESTADO_ACTUAL     <> 3 
  AND TO_CHAR(FECHANCTO,'MM')    =  s$mesNomina$s
ORDER BY TO_CHAR(FECHANCTO,'DD')
