SELECT
	COD,
	ID_CENTRO_DE_COSTO,
	NOMBRE_CENTRO_DE_COSTO,
	ID_DE_PROCESO,
	ANO,
	MES,
	CARGO,
	NOMBRECARGO,
	DEPENDENCIA,
	NOMBREDEPENDENCIA,
	PERIODO,
	NOM_EMPLEADO,
	CEDULA,
	DEVENGOS,
	DESCUENTOS,
	NETO,
	NVL(C002,0)C002,
	NVL(C188,0)C188,
	NVL(C080,0)C080,
	NVL(C073,0)C073,
	NVL(C379,0)C379,
	NVL(C171,0)C171,
	NVL(C174,0)C174,
	NVL(C155,0)C155,
	NVL(C183,0)C183,
	NVL(C097,0)C097,
	NVL(C113,0)C113,
	NVL(C114,0)C114,
	NVL(C118,0)C118,
	NVL(C115,0)C115,
	NVL(C129,0)C129,
	NVL(C125,0)C125,
	NVL(C699,0)C699,
	NVL(C799,0)C799,
	NVL(C140,0)C140,
	NVL(C144,0)C144,
	NVL(C009,0)C009
	FROM
  	(SELECT 
 		HISTORICOS.VALOR,
  		HISTORICOS.ID_DE_EMPLEADO AS COD,
    	HISTORICOS.ID_DE_CONCEPTO,
    	CENTRO_COSTO.CODIGO ID_CENTRO_DE_COSTO,
    	CENTRO_COSTO.NOMBRE NOMBRE_CENTRO_DE_COSTO,
   	    MAX(HISTORICOS.ID_DE_PROCESO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) ID_DE_PROCESO,
        MAX (HISTORICOS.ANO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) ANO,
	    MAX(HISTORICOS.MES) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) MES,
	    MAX(PERSONAL.ID_DE_CARGO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) CARGO,
	    MAX(CARGOS.NOMBRE_DEL_CARGO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NOMBRECARGO,
	    MAX(PERSONAL.DEPENDENCIA) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) DEPENDENCIA,
	    MAX(DEPENDENCIA.NOMBRE ) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NOMBREDEPENDENCIA,
	    MAX(HISTORICOS.PERIODO ) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) PERIODO,
	    MAX(PERSONAL.NOMBRECOMPLETO ) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NOM_EMPLEADO,
	    MAX(PERSONAL.NUMERO_DCTO ) AS CEDULA,
	    SUM(
	    CASE
	      WHEN CONCEPTOS.CLASE IN (3)
	      THEN HISTORICOS.VALOR
	      ELSE 0
	    END) DEVENGOS,
	    SUM(
	    CASE
	      WHEN CONCEPTOS.CLASE IN (5)
	      THEN HISTORICOS.VALOR
	      ELSE 0
	    END) DESCUENTOS,
	    SUM(
	    CASE
	      WHEN CONCEPTOS.CLASE IN (3)
	      THEN HISTORICOS.VALOR
	      ELSE 0
	    END -
	    CASE
	      WHEN CONCEPTOS.CLASE IN (5)
	      THEN HISTORICOS.VALOR
	      ELSE 0
	    END)NETO
	FROM HISTORICOS
	  INNER JOIN CONCEPTOS
	  	 ON HISTORICOS.COMPANIA        = CONCEPTOS.COMPANIA
	  	AND HISTORICOS.ID_DE_CONCEPTO  = CONCEPTOS.ID_DE_CONCEPTO
	  INNER JOIN PERIODOS
	  	 ON HISTORICOS.COMPANIA       = PERIODOS.COMPANIA
	 	AND HISTORICOS.ID_DE_PROCESO  = PERIODOS.ID_DE_PROCESO
	  	AND HISTORICOS.ANO            = PERIODOS.ANO
	  	AND HISTORICOS.MES            = PERIODOS.MES
	  	AND HISTORICOS.PERIODO        = PERIODOS.PERIODO
	  INNER JOIN PERSONAL
	  	 ON HISTORICOS.COMPANIA        = PERSONAL.COMPANIA
	  	AND HISTORICOS.ID_DE_EMPLEADO  = PERSONAL.ID_DE_EMPLEADO
	  LEFT JOIN CARGOS
	 	 ON PERSONAL.COMPANIA     = CARGOS.COMPANIA
	  	AND PERSONAL.ID_DE_CARGO  = CARGOS.ID_DE_CARGO
	  INNER JOIN DEPENDENCIA
	  	 ON PERSONAL.COMPANIA     = DEPENDENCIA.COMPANIA
	    	AND PERSONAL.DEPENDENCIA  = DEPENDENCIA.CODIGO
	  INNER JOIN CENTRO_COSTO
	  	 ON PERSONAL.COMPANIA           = CENTRO_COSTO.COMPANIA
	  	AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
	  WHERE PERSONAL.COMPANIA           = s$compania$s
	  	AND HISTORICOS.ID_DE_PROCESO    = s$proceso$s
	  	AND HISTORICOS.ANO              = s$ano$s
	  	AND HISTORICOS.MES              = s$mes$s
	  	AND HISTORICOS.PERIODO          = s$periodo$s
	  	AND PERIODOS.ACUMULADO          NOT IN(0)
	 GROUP BY 
	  HISTORICOS.VALOR,
	  HISTORICOS.ID_DE_EMPLEADO,
	  CENTRO_COSTO.CODIGO,
	  CENTRO_COSTO.NOMBRE,
	  HISTORICOS.ID_DE_CONCEPTO
  ) PIVOT ( SUM(VALOR) FOR ID_DE_CONCEPTO IN (9 AS "C009", 2 AS "C002",188 AS "C188",80 AS "C080",73 AS "C073",379 AS "C379",171 AS "C171",174 AS "C174",155 AS "C155", 183 AS "C183" , 97 AS "C097" , 113 AS "C113",114 AS "C114", 118 AS "C118",115 AS "C115",129 AS "C129",125 AS "C125", 699 AS "C699", 799 AS "C799", 140 AS "C140", 144 AS "C144") )
