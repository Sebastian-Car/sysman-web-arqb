SELECT  
        MAX(HISTORICOS.COMPANIA) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS COMPANIA,
        MAX(HISTORICOS.ID_DE_PROCESO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS ID_DE_PROCESO,
        MAX(HISTORICOS.ANO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS ANO,
        MAX(HISTORICOS.MES) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS MES,
        MAX(HISTORICOS.PERIODO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS PERIODO,
        ESTABLECIMIENTOS_DOCENTES.CODIGO AS ESTABLECIMIENTO, 
        ESTABLECIMIENTOS_DOCENTES.NOMBRE AS NOMBRE_ESTABLECIMIENTO, 
        PERSONAL.NUMERO_DCTO AS CEDULA, 
        PERSONAL.NOMBRECOMPLETO AS NOM_EMPLEADO,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('001') THEN HISTORICOS.VALOR ELSE 0 END) SUELDOBASICO,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('160') THEN HISTORICOS.VALOR ELSE 0 END) PRIMASEMESTRAL,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('158') THEN HISTORICOS.VALOR ELSE 0 END) PRIMANAVIDAD,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('362') THEN HISTORICOS.VALOR ELSE 0 END) OTROSINGRESOS,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('097') THEN HISTORICOS.VALOR ELSE 0 END) RENUMERACION,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('125') THEN HISTORICOS.VALOR ELSE 0 END) RETEFUENTE,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('123') THEN HISTORICOS.VALOR ELSE 0 END) ADICIONAL,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('129') THEN HISTORICOS.VALOR ELSE 0 END) SINDICATO,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('600') THEN HISTORICOS.VALOR ELSE 0 END) SALUD,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('609') THEN HISTORICOS.VALOR ELSE 0 END) COOPSER,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('610') THEN HISTORICOS.VALOR ELSE 0 END) UNIVALLE,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('611') THEN HISTORICOS.VALOR ELSE 0 END) DESARROLLO,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('612') THEN HISTORICOS.VALOR ELSE 0 END) HOSPITAL,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('799') THEN HISTORICOS.VALOR ELSE 0 END) EMBARGO,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('616') THEN HISTORICOS.VALOR ELSE 0 END) OTROS,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('140') THEN HISTORICOS.VALOR ELSE 0 END) DEDUCCIONES,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('144') THEN HISTORICOS.VALOR ELSE 0 END) NETOAPAGAR
FROM HISTORICOS 
  INNER JOIN CONCEPTOS 
    ON HISTORICOS.COMPANIA        = CONCEPTOS.COMPANIA  
    AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO 
  INNER JOIN PERIODOS
    ON HISTORICOS.COMPANIA        = PERIODOS.COMPANIA
    AND HISTORICOS.ID_DE_PROCESO  = PERIODOS.ID_DE_PROCESO
    AND HISTORICOS.ANO            = PERIODOS.ANO
    AND HISTORICOS.MES            = PERIODOS.MES
    AND HISTORICOS.PERIODO        = PERIODOS.PERIODO 
  INNER JOIN PERSONAL 
    ON HISTORICOS.COMPANIA            = PERSONAL.COMPANIA  
    AND HISTORICOS.ID_DE_EMPLEADO     = PERSONAL.ID_DE_EMPLEADO 
  LEFT JOIN ESTABLECIMIENTOS_DOCENTES 
    ON PERSONAL.COMPANIA                = ESTABLECIMIENTOS_DOCENTES.COMPANIA 
    AND PERSONAL.CODIGO_ESTABLECIMIENTO = ESTABLECIMIENTOS_DOCENTES.CODIGO
WHERE HISTORICOS.COMPANIA		=	s$compania$s
  AND HISTORICOS.ID_DE_PROCESO	=	s$proceso$s 
  AND HISTORICOS.ANO 			=	s$ano$s
  AND HISTORICOS.MES 			=	s$mes$s
  AND HISTORICOS.PERIODO		=	s$periodo$s 
  AND PERIODOS.ACUMULADO NOT IN(0) 
GROUP BY  ESTABLECIMIENTOS_DOCENTES.CODIGO ,
          ESTABLECIMIENTOS_DOCENTES.NOMBRE ,
          PERSONAL.NUMERO_DCTO ,
          PERSONAL.NOMBRECOMPLETO
ORDER BY ESTABLECIMIENTOS_DOCENTES.CODIGO
