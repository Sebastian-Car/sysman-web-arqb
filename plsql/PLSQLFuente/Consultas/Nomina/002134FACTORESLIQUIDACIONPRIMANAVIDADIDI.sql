SELECT PERSONAL.ID_DE_EMPLEADO,
  PERSONAL.NOMBRECOMPLETO,
  PERSONAL.FONDO_CESANTIAS,
  PERSONAL.NUMERO_DCTO,
  CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO,
  DEPENDENCIA.NOMBRE AS DEPENDENCIA,
  CARGOS.NOMBRE_DEL_CARGO,
  HISTORICOS.ID_DE_PROCESO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '853' THEN HISTORICOS.VALOR ELSE 0 END) AS SUELDO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '845' THEN HISTORICOS.VALOR ELSE 0 END) AS ANTIGUEDAD,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '843' THEN HISTORICOS.VALOR ELSE 0 END) AS GASTOS,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '841' THEN HISTORICOS.VALOR ELSE 0 END) AS TRANS,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '842' THEN HISTORICOS.VALOR ELSE 0 END) AS ALIM,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '155' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMAVAC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '849' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMAVAC1,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '851' THEN HISTORICOS.VALOR ELSE 0 END)  AS BONIF,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '985' THEN HISTORICOS.VALOR ELSE 0 END) AS SUELDOBASE,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '852' THEN HISTORICOS.VALOR ELSE 0 END) AS QUINQUENIO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '855' THEN HISTORICOS.VALOR ELSE 0 END) AS MESESPNAV,
  ROUND(SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '849' THEN HISTORICOS.VALOR ELSE 0 END)/12,0)  AS DOCPVAC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '852' THEN HISTORICOS.VALOR ELSE 0 END) /12          AS DOCQUIN,
  ROUND(SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '851' THEN HISTORICOS.VALOR ELSE 0 END) /12,0) AS DOCBON,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '846' THEN HISTORICOS.VALOR ELSE 0 END) AS TOTMESES,
  MAX (TO_CHAR(HISTORICOS.FECHA,'DD/MM/YYYY'))  AS FECHALIQ,
  PERSONAL.ID_CENTRO_DE_COSTO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '347' THEN HISTORICOS.VALOR ELSE 0 END) AS DIASLICENC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '857' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMANAV,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '158' THEN HISTORICOS.VALOR ELSE 0 END) AS NAVPROPORC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '850' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMASEM,
  ROUND(SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '850' THEN HISTORICOS.VALOR ELSE 0 END)/12,0) AS DOCPSEM,
  TO_CHAR(PERSONAL.FECHA_DE_RETIRO,'DD/MM/YYYY')FECHA_DE_RETIRO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '72' THEN HISTORICOS.VALOR ELSE 0 END) AS RETPRIMASEM,
  ROUND(SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '72' THEN HISTORICOS.VALOR ELSE 0 END)/12,0) AS RETDOCPSEM,
  CARGOS.CODIGOCARGO,
  CARGOS.GRADO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '803'THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMATEC,
  '01/01/'
 || s$anio$s AS FECHAINIPN
FROM HISTORICOS
INNER JOIN PERSONAL
   ON HISTORICOS.COMPANIA        = PERSONAL.COMPANIA
  AND HISTORICOS.ID_DE_EMPLEADO  = PERSONAL.ID_DE_EMPLEADO
INNER JOIN CARGOS
    ON CARGOS.COMPANIA    = PERSONAL.COMPANIA
   AND CARGOS.ID_DE_CARGO = PERSONAL.ID_DE_CARGO
INNER JOIN CENTRO_COSTO
    ON CENTRO_COSTO.COMPANIA   = PERSONAL.COMPANIA
   AND CENTRO_COSTO.ANO       = PERSONAL.ANO
   AND CENTRO_COSTO.CODIGO = PERSONAL.ID_CENTRO_DE_COSTO
INNER JOIN DEPENDENCIA
    ON  DEPENDENCIA.COMPANIA = PERSONAL.COMPANIA
   AND DEPENDENCIA.CODIGO   = PERSONAL.DEPENDENCIA
WHERE 
  HISTORICOS.COMPANIA = s$compania$s
  AND PERSONAL.FONDO_CESANTIAS >='1'
  AND HISTORICOS.ID_DE_PROCESO    =s$proceso$s
  AND HISTORICOS.ANO      = s$anio$s
  AND HISTORICOS.MES      = s$mes$s
  AND HISTORICOS.PERIODO  = s$periodo$s
  AND HISTORICOS.ID_DE_CONCEPTO IN('841','842','843','844','845','846','847','849','848','850','851','852','853','855','856','857','072','985','832','803','811','989','851','158')
GROUP BY PERSONAL.ID_DE_EMPLEADO,
  PERSONAL.NOMBRECOMPLETO,
  PERSONAL.FONDO_CESANTIAS,
  PERSONAL.NUMERO_DCTO,
  CENTRO_COSTO.NOMBRE,
  DEPENDENCIA.NOMBRE,
  CARGOS.NOMBRE_DEL_CARGO,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  PERSONAL.ID_CENTRO_DE_COSTO,
  PERSONAL.FECHA_DE_RETIRO,
  CARGOS.CODIGOCARGO,
  CARGOS.GRADO,
  '01/01/'
 || s$anio$s,
  HISTORICOS.PERIODO
HAVING 
 (SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '857' THEN HISTORICOS.VALOR ELSE 0 END)<>0)
ORDER BY PERSONAL.NOMBRECOMPLETO