SELECT  
	HISTORICOS.ANO,  
	UPPER(PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES)) MES ,  
	PERSONAL.ID_CENTRO_DE_COSTO ,  
	CENTRO_COSTO.NOMBRE NCENTRO,  
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = 112 THEN  HISTORICOS.VALOR  ELSE 0 END ) AS IBC,  
	SUM(( CASE WHEN HISTORICOS.ID_DE_CONCEPTO =111 THEN  HISTORICOS.VALOR  ELSE 0 END )) AS RIESGOPATRONO,  
	SUM(( CASE WHEN  HISTORICOS.ID_DE_CONCEPTO =211 THEN  HISTORICOS.VALOR  ELSE 0 END )) AS DTOARP,  
	SUM(( CASE WHEN  HISTORICOS.ID_DE_CONCEPTO =111 THEN  HISTORICOS.VALOR  ELSE 0 END ))-  
	SUM(( CASE WHEN  HISTORICOS.ID_DE_CONCEPTO =211 THEN  HISTORICOS.VALOR  ELSE 0 END )) AS TOTAL,  
	V_FONDO_DE_RIESGOS.NIT,  
	V_FONDO_DE_RIESGOS.NOMBRE_FONDO_RIESGOS NOMBRE_FONDO 
FROM PERSONAL  
  INNER JOIN CENTRO_COSTO ON  
             CENTRO_COSTO.COMPANIA = PERSONAL.COMPANIA 
         AND CENTRO_COSTO.ANO      = PERSONAL.ANO     
         AND CENTRO_COSTO.CODIGO   = PERSONAL.ID_CENTRO_DE_COSTO  
	INNER JOIN V_FONDO_DE_RIESGOS ON  
             V_FONDO_DE_RIESGOS.COMPANIA      = PERSONAL.COMPANIA 
         AND V_FONDO_DE_RIESGOS.FONDO_RIESGOS = PERSONAL.FONDO_RIESGOS 
	INNER JOIN HISTORICOS ON  
             PERSONAL.COMPANIA       = HISTORICOS.COMPANIA  
         AND PERSONAL.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO 	 
WHERE HISTORICOS.COMPANIA=s$compania$s 
	AND HISTORICOS.ID_DE_CONCEPTO IN (112,111,211) 
	AND HISTORICOS.ANO= s$anioNomina$s 
	AND HISTORICOS.MES= s$mesNomina$s 
GROUP BY  
	HISTORICOS.ANO, 
	HISTORICOS.MES,  
	PERSONAL.ID_CENTRO_DE_COSTO,  
	CENTRO_COSTO.NOMBRE,  
	V_FONDO_DE_RIESGOS.NIT,  
	V_FONDO_DE_RIESGOS.NOMBRE_FONDO_RIESGOS
