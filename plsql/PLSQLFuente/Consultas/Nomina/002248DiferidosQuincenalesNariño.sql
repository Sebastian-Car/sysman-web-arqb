select
	CONCEPTOS.ID_DE_CONCEPTO,
	PERIODOS.FECHAFINAL,
	PERSONAL.ID_DE_EMPLEADO,
	PERSONAL.NUMERO_DCTO,
	PERSONAL.APELLIDO1 || ' ' || PERSONAL.APELLIDO2 || ' ' || PERSONAL.NOMBRES as NOMBRECOMPLETO,
	CONCEPTOS.NOMBRE_CONCEPTO,
	sum(FINANCIABLES_DE_NOMINA.VALOR_CUOTA) as VALOR_CUOTA,
	sum(FINANCIABLES_DE_NOMINA.SALDO) as SALDO,
	avg(FINANCIABLES_DE_NOMINA.VALDESCUENTO) as VALDESCUENTO,
	FINANCIABLES_DE_NOMINA.NUMERO_DE_CUOTAS,
	sum((FINANCIABLES_DE_NOMINA.SALDO)-(FINANCIABLES_DE_NOMINA.VALDESCUENTO)) as SALDOPRESTAMO,
	FINANCIABLES_DE_NOMINA.MONTO_INICIAL,
	FINANCIABLES_DE_NOMINA.NLIBRANZA,
	FINANCIABLES_DE_NOMINA.FECHA_LIBRANZA,
	FINANCIABLES_DE_NOMINA.TOTAL_CUOTAS,
	COMPANIA.CODIGO
from
	((FINANCIABLES_DE_NOMINA
left join PERSONAL on
	(FINANCIABLES_DE_NOMINA.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO)
	and (FINANCIABLES_DE_NOMINA.COMPANIA = PERSONAL.COMPANIA))
left join CONCEPTOS on
	(FINANCIABLES_DE_NOMINA.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO)
	and (FINANCIABLES_DE_NOMINA.COMPANIA = CONCEPTOS.COMPANIA))
left join PERIODOS on
	(FINANCIABLES_DE_NOMINA.COMPANIA = PERIODOS.COMPANIA)
	and (FINANCIABLES_DE_NOMINA.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO)
	and (FINANCIABLES_DE_NOMINA.ANO = PERIODOS.ANO)
	and (FINANCIABLES_DE_NOMINA.MES = PERIODOS.MES)
	and (FINANCIABLES_DE_NOMINA.PERIODO = PERIODOS.PERIODO)
left join COMPANIA on
	CONCEPTOS.COMPANIA = COMPANIA.CODIGO
where
	COMPANIA.CODIGO = s$compania$s
	and CONCEPTOS.ID_DE_CONCEPTO between s$conceptoI$s and s$conceptoF$s
	and PERSONAL.ID_DE_EMPLEADO between s$empleadoI$s and s$empleadoF$s
	and FINANCIABLES_DE_NOMINA.ANO between s$anoI$s and s$anoF$s
	and FINANCIABLES_DE_NOMINA.MES between s$mesI$s and s$mesF$s
	and FINANCIABLES_DE_NOMINA.PERIODO between s$periodoI$s and s$periodoF$s
group by
	CONCEPTOS.ID_DE_CONCEPTO,
	PERIODOS.FECHAFINAL,
	PERSONAL.ID_DE_EMPLEADO,
	PERSONAL.NUMERO_DCTO,
	PERSONAL.APELLIDO1 || ' ' || PERSONAL.APELLIDO2 || ' ' || PERSONAL.NOMBRES,
	CONCEPTOS.NOMBRE_CONCEPTO,
	FINANCIABLES_DE_NOMINA.NUMERO_DE_CUOTAS,
	FINANCIABLES_DE_NOMINA.MONTO_INICIAL,
	FINANCIABLES_DE_NOMINA.NLIBRANZA,
	FINANCIABLES_DE_NOMINA.FECHA_LIBRANZA,
	FINANCIABLES_DE_NOMINA.TOTAL_CUOTAS,
	COMPANIA.CODIGO
order by
	CONCEPTOS.ID_DE_CONCEPTO ,
	PERIODOS.FECHAFINAL DESC,
    NOMBRECOMPLETO;
	