WITH PERSONAL_CHEQUEO_FONDOS AS
  (SELECT PERSONAL.COMPANIA COMPANIA1,
    PERSONAL.ID_DE_EMPLEADO ID_DE_EMPLEADO1,
    V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD EPS,
    V_FONDO_DE_PENSIONES.NOMBRE_DEL_FONDO AFC,
    V_CAJA_COMPENSACION.NOMBRE_CAJA CAJA,
    V_FONDO_DE_CESANTIAS.NOMBRE_FONDO_CESANTIAS FC,
    V_FONDO_DE_RIESGOS.NOMBRE_FONDO_RIESGOS ARP
  FROM PERSONAL
  LEFT JOIN V_FONDO_DE_PENSIONES
    ON  PERSONAL.COMPANIA     = V_FONDO_DE_PENSIONES.COMPANIA
    AND PERSONAL.ID_DEL_FONDO = V_FONDO_DE_PENSIONES.ID_DEL_FONDO
  LEFT JOIN V_FONDO_DE_SALUD
    ON  PERSONAL.COMPANIA    = V_FONDO_DE_SALUD.COMPANIA
    AND PERSONAL.FONDO_SALUD = V_FONDO_DE_SALUD.FONDO_SALUD
  LEFT JOIN V_CAJA_COMPENSACION
    ON  PERSONAL.COMPANIA          = V_CAJA_COMPENSACION.COMPANIA
    AND PERSONAL.CAJA_COMPENSACION = V_CAJA_COMPENSACION.CAJA_COMPENSACION
  LEFT JOIN V_FONDO_DE_CESANTIAS
    ON PERSONAL.COMPANIA         = V_FONDO_DE_CESANTIAS.COMPANIA
    AND PERSONAL.FONDO_CESANTIAS = V_FONDO_DE_CESANTIAS.FONDO_CESANTIAS
  LEFT JOIN V_FONDO_DE_RIESGOS
    ON PERSONAL.COMPANIA        = V_FONDO_DE_RIESGOS.COMPANIA
    AND PERSONAL.FONDO_RIESGOS  = V_FONDO_DE_RIESGOS.FONDO_RIESGOS
  WHERE PERSONAL.COMPANIA       = s$compania$s
   AND PERSONAL.ID_DE_EMPLEADO = s$idEmpleado$s
  )
SELECT PERSONAL.COMPANIA,
  COMPANIA.SIGLACOMPANIA,
  PERSONAL.ID_DE_EMPLEADO,
  PERSONAL.FECHATERCONTRATO,
  PERSONAL.APELLIDO1,
  PERSONAL.APELLIDO2,
  PERSONAL.NOMBRES,
  PERSONAL.NUMERO_DCTO,
  PERSONAL.DCTO_IDENTIDAD,
  DOCUMENTOS.NUMERO DESCRIPCION,
  DEPARTAMENTO.NOMBRE DEP_CEDULA,
  CIUDAD.NOMBRE MUNICIPIOCED,
  PERSONAL.CERTIFICADOJUDICIAL,
  PERSONAL.LICENCIACONDUCCION,
  PERSONAL.LIBRETA_MILITAR,
  PERSONAL.FECHANCTO,
  DEPARTAMENTONAC.NOMBRE DPTONAC,
  CIUDADNAC.NOMBRE CIUDADNAC,
  PERSONAL.SEXO,
  ESTADO_CIVIL.DESCRIPCION ESTADOCIVIL,
  PERSONAL.FECHA_DE_INGRESO,
  CARGOS.NOMBRE_DEL_CARGO,
  PERSONAL.ANO,
  DEPENDENCIA.NOMBRE DEPENDENCIA,
  CENTRO_COSTO.NOMBRE NOMBRE_CENTRO_DE_COSTO,
  FORMANOMBRAMIENTO.NOMBREFORMA FORMANOMBRAMIENTO,
  CASE PERSONAL.AREAMISOADM
    WHEN 'MI'
    THEN 'MISIONAL'
    ELSE 'ADMINISTRATIVA'
  END AREADM,
  CASE PERSONAL.TIPOACTIVIDAD
    WHEN 'GA'
    THEN 'Gasto Administrativo'
    WHEN 'GPS'
    THEN 'Gasto Público Social'
    ELSE 'Gasto de Inversión Social'
  END TIPOACTIVIDAD,
  BANCO.NOMBREBANCO BANCO,
  PERSONAL.CUENTA,
  CASE PERSONAL.TIPOCUENTA
    WHEN 'A'
    THEN 'Ahorro'
    ELSE 'Corriente'
  END TIPOCUENTA,
  CASE PERSONAL.REGIMEN
    WHEN 1
    THEN 'Retroactivas'
    WHEN 2
    THEN 'Ley 50/90'
    ELSE 'Ninguno'
  END REGIMENCES,
  PATRONALES.FACTOR FACTORRIESGOS,
  CASE PERSONAL.GRUPOCONTABLE
    WHEN 'A'
    THEN 'Administración'
    WHEN 'V'
    THEN 'Ventas/Operación'
    WHEN 'P'
    THEN 'Producción/Pensionados'
    WHEN 'O'
    THEN 'Otro'
    WHEN 'S'
    THEN 'Supernumerario'
  END GRUPOCONTABLE,
  CASE PERSONAL.PROCESORETENCION
    WHEN 1
    THEN 'Porcentaje por Tabla'
    ELSE 'Porcentaje Fijo'
  END TIPORETENCION,
  PERSONAL.DIRECCION,
  PERSONAL.TELEFONOS,
  PERSONAL.CALZADO,
  PERSONAL.CAMISA,
  PERSONAL.PANTALON,
  PERSONAL.CHAQUETA,
  PERSONAL.GRUPOSANGINEO,
  PERSONAL.RH,
  CIUDADHAB.NOMBRE CIUDADHAB,
  CASE WHEN PERSONAL.ESTADO_ACTUAL = 2 THEN TIPO_VINCULACION.NOMBRE_PENSIONADO ELSE TIPO_VINCULACION.NOMBRE END  TIPOVINCULACION,
  PERSONAL.BARRIO,
  PCK_NOMINA_COM6.FC_CUALSALARIO(PERSONAL.COMPANIA, PERSONAL.ID_DE_EMPLEADO) SALARIO_BASE,
  TIPOS_DE_EMPLEADO.NOMBRE_TIPO NOMBRE_TIPO1,
  PERSONAL_CHEQUEO_FONDOS.EPS,
  PERSONAL_CHEQUEO_FONDOS.AFC,
  PERSONAL_CHEQUEO_FONDOS.CAJA,
  PERSONAL_CHEQUEO_FONDOS.FC,
  PERSONAL_CHEQUEO_FONDOS.ARP,
  ESCALAFON.NOMBRE NIVEL,
  CASE PERSONAL.TIPO_SALARIO
    WHEN 'F'
    THEN 'Fijo'
    ELSE 'Variable'
  END TIPO_SALARIO
FROM PERSONAL
LEFT JOIN DOCUMENTOS
  ON  PERSONAL.COMPANIA       = DOCUMENTOS.COMPANIA
  AND PERSONAL.DCTO_IDENTIDAD = DOCUMENTOS.DOCUMENTO
LEFT JOIN CIUDAD
  ON PERSONAL.PAIS_CED          = CIUDAD.PAIS
  AND PERSONAL.DEPARTAMENTO_CED = CIUDAD.DEPARTAMENTO
  AND PERSONAL.EXPEDIDA         = CIUDAD.CODIGO
LEFT JOIN DEPARTAMENTO
  ON CIUDAD.PAIS          = DEPARTAMENTO.PAIS
  AND CIUDAD.DEPARTAMENTO = DEPARTAMENTO.CODIGO
LEFT JOIN CIUDAD CIUDADNAC
  ON PERSONAL.PAIS_NAC          = CIUDADNAC.PAIS
  AND PERSONAL.DEPARTAMENTO_NAC = CIUDADNAC.DEPARTAMENTO
  AND PERSONAL.CIUDAD_NAC       = CIUDADNAC.CODIGO
LEFT JOIN DEPARTAMENTO DEPARTAMENTONAC
  ON CIUDADNAC.PAIS          = DEPARTAMENTONAC.PAIS
  AND CIUDADNAC.DEPARTAMENTO = DEPARTAMENTONAC.CODIGO
LEFT JOIN CIUDAD CIUDADHAB
  ON PERSONAL.PAIS_HAB          = CIUDADHAB.PAIS
  AND PERSONAL.DEPARTAMENTO_HAB = CIUDADHAB.DEPARTAMENTO
  AND PERSONAL.CIUDAD_HAB       = CIUDADHAB.CODIGO
LEFT JOIN ESTADO_CIVIL
  ON PERSONAL.COMPANIA      = ESTADO_CIVIL.COMPANIA
  AND PERSONAL.ESTADO_CIVIL = ESTADO_CIVIL.ESTADO_CIVIL
LEFT JOIN CARGOS
  ON  PERSONAL.COMPANIA    = CARGOS.COMPANIA
  AND PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO
LEFT JOIN DEPENDENCIA
  ON PERSONAL.COMPANIA     = DEPENDENCIA.COMPANIA
  AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
LEFT JOIN CENTRO_COSTO
  ON PERSONAL.COMPANIA            = CENTRO_COSTO.COMPANIA
  AND PERSONAL.ANO                = CENTRO_COSTO.ANO
  AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
LEFT JOIN FORMANOMBRAMIENTO
  ON  PERSONAL.COMPANIA   = FORMANOMBRAMIENTO.COMPANIA
  AND PERSONAL.DE_CARRERA = FORMANOMBRAMIENTO.IDFORMA
LEFT JOIN BANCO
  ON  PERSONAL.COMPANIA  = BANCO.COMPANIA
  AND PERSONAL.BANCO     = BANCO.BANCO
LEFT JOIN PATRONALES
  ON  PERSONAL.COMPANIA       = PATRONALES.COMPANIA
  AND PERSONAL.NUMEROPATRONAL = PATRONALES.SUCURSAL
LEFT JOIN COMPANIA
  ON PERSONAL.COMPANIA = COMPANIA.CODIGO
LEFT JOIN TIPOS_DE_EMPLEADO 
  ON PERSONAL.COMPANIA    = TIPOS_DE_EMPLEADO.COMPANIA
  AND PERSONAL.ID_DE_TIPO = TIPOS_DE_EMPLEADO.ID_DE_TIPO
LEFT JOIN PERSONAL_CHEQUEO_FONDOS
  ON  PERSONAL.COMPANIA       = PERSONAL_CHEQUEO_FONDOS.COMPANIA1
  AND PERSONAL.ID_DE_EMPLEADO = PERSONAL_CHEQUEO_FONDOS.ID_DE_EMPLEADO1
LEFT JOIN ESCALAFON
  ON PERSONAL.ESCALAFON       = ESCALAFON.CODIGO
  AND PERSONAL.COMPANIA       = ESCALAFON.COMPANIA
  LEFT JOIN TIPO_VINCULACION 
  ON PERSONAL.TIPOVINCULACION =TIPO_VINCULACION.CODIGO
WHERE PERSONAL.COMPANIA       = s$compania$s
  AND PERSONAL.ID_DE_EMPLEADO = s$idEmpleado$s
