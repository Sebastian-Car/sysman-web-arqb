    WHEN 5 THEN 'Descuento' 
    WHEN 6 THEN 'Total Deducido' 
    WHEN 7 THEN 'Neto a Pagar' 
    WHEN 8 THEN 'Contable' 
    WHEN 9 THEN 'Anticipados' 
    WHEN 99 THEN 'Bases' 
