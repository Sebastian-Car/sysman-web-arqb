  CASE C.REPORTEEPS WHEN 0 THEN ' ' WHEN -1  THEN 'x' END AUTOLIQUIDACION,
  CASE C.FACTOR_PARAFISCAL WHEN 0 THEN ' ' WHEN -1  THEN 'x' END PARAFISCAL,
  CASE C.FACTOR_RETEFUENTE WHEN 0 THEN ' ' WHEN -1  THEN 'x' END RETEFUENTE,
