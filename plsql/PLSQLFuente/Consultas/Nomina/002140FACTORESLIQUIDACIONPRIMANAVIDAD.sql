SELECT PERSONAL.ID_DE_EMPLEADO,
 TRIM(APELLIDO1) || ' ' || TRIM(APELLIDO2) || ' ' || TRIM(NOMBRES) AS NOMCOMPLETO,
 PERSONAL.FONDO_CESANTIAS, 
 PERSONAL.NUMERO_DCTO, 
 CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO,
 DEPENDENCIA.NOMBRE, 
 CARGOS.NOMBRE_DEL_CARGO, 
 HISTORICOS.ID_DE_PROCESO, 
 HISTORICOS.ANO, 
PCK_SYSMAN_UTL.FC_NOMBRE_MES(MES) AS MES,
 HISTORICOS.PERIODO, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='853' THEN HISTORICOS.VALOR ELSE 0 END) AS SUELDO, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='845' THEN HISTORICOS.VALOR ELSE 0 END) AS ANTIGUEDAD,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='844' THEN HISTORICOS.VALOR ELSE 0 END) AS GASTOS,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='841' THEN HISTORICOS.VALOR ELSE 0 END) AS TRANS, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='842' THEN HISTORICOS.VALOR ELSE 0 END) AS ALIM, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='849' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMAVAC, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='848' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMATEC, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='851' THEN HISTORICOS.VALOR ELSE 0 END) AS BONIF, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='856' THEN HISTORICOS.VALOR ELSE 0 END) AS SUELDOBASE, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='850' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMASEM, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='834' THEN HISTORICOS.VALOR ELSE 0 END) AS QUINQUENIO, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='355' THEN HISTORICOS.VALOR ELSE 0 END) AS DIASPN, 
 ROUND( SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='849' THEN HISTORICOS.VALOR ELSE 0 END)/12,0) AS DOCPVAC, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='834' THEN HISTORICOS.VALOR ELSE 0 END)/12 AS DOCQUIN,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='851' THEN HISTORICOS.VALOR ELSE 0 END)/12 AS DOCBON, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='355' THEN HISTORICOS.VALOR ELSE 0 END) AS TOTDIAS, 
 MAX(TO_CHAR(HISTORICOS.FECHA,'DD/MM/YYYY')) AS FECHALIQ, 
 PERSONAL.ID_CENTRO_DE_COSTO,
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='347' THEN HISTORICOS.VALOR ELSE 0 END) AS DIASLICENC, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='158' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMANAV, 
 SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='503' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='160' THEN HISTORICOS.VALOR ELSE 0 END) AS SEMESTPROPORC, 
 TO_CHAR(PERSONAL.FECHA_DE_INGRESO,'DD/MM/YYYY') AS FECHA_DE_INGRESO,
 PERSONAL.ID_DE_CARGO, 
 CARGOSE.NOMBRE_DEL_CARGO AS NOMBRE_DEL_CARGO_ESCALERA, 
 CARGOST.NOMBRE_DEL_CARGO AS NOMBRE_DEL_CARGOTEMPORAL,
 PERSONAL.ID_DE_CARGOESCALERA, 
 PERSONAL.ID_DE_CARGO_TEMPORAL
FROM HISTORICOS 
INNER JOIN PERSONAL 
ON PERSONAL.COMPANIA=HISTORICOS.COMPANIA
AND PERSONAL.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
INNER JOIN DEPENDENCIA
ON DEPENDENCIA.CODIGO = PERSONAL.DEPENDENCIA
AND DEPENDENCIA.COMPANIA=PERSONAL.COMPANIA
INNER JOIN CENTRO_COSTO 
ON CENTRO_COSTO.CODIGO= PERSONAL.ID_CENTRO_DE_COSTO 
AND CENTRO_COSTO.COMPANIA=PERSONAL.COMPANIA
AND CENTRO_COSTO.ANO=HISTORICOS.ANO
LEFT JOIN CARGOS 
ON CARGOS.ID_DE_CARGO = PERSONAL.ID_DE_CARGO 
AND CARGOS.COMPANIA=PERSONAL.COMPANIA
LEFT JOIN CARGOS CARGOST
ON CARGOST.ID_DE_CARGO = PERSONAL.ID_DE_CARGO_TEMPORAL 
AND CARGOST.COMPANIA=PERSONAL.COMPANIA
LEFT JOIN CARGOS CARGOSE
ON CARGOSE.ID_DE_CARGO = PERSONAL.ID_DE_CARGOESCALERA 
AND CARGOSE.COMPANIA=PERSONAL.COMPANIA
WHERE
(HISTORICOS.ID_DE_CONCEPTO='158' 
OR HISTORICOS.ID_DE_CONCEPTO='853' 
OR HISTORICOS.ID_DE_CONCEPTO='841' 
OR HISTORICOS.ID_DE_CONCEPTO='842' 
OR HISTORICOS.ID_DE_CONCEPTO='845' 
OR HISTORICOS.ID_DE_CONCEPTO='844' 
OR HISTORICOS.ID_DE_CONCEPTO='849' 
OR HISTORICOS.ID_DE_CONCEPTO='848' 
OR HISTORICOS.ID_DE_CONCEPTO='851' 
OR HISTORICOS.ID_DE_CONCEPTO='850' 
OR HISTORICOS.ID_DE_CONCEPTO='355' 
OR HISTORICOS.ID_DE_CONCEPTO='856'
)
AND HISTORICOS.ANO=s$ano$s
AND HISTORICOS.MES=s$mes$s
AND HISTORICOS.PERIODO=s$periodo$s
AND HISTORICOS.ID_DE_PROCESO=s$idProceso$s
GROUP BY PERSONAL.ID_DE_EMPLEADO, 
TRIM(APELLIDO1) || ' ' || TRIM(APELLIDO2) || ' ' || TRIM(NOMBRES), 
PERSONAL.FONDO_CESANTIAS, 
PERSONAL.NUMERO_DCTO, 
CENTRO_COSTO.NOMBRE, 
DEPENDENCIA.NOMBRE, 
CARGOS.NOMBRE_DEL_CARGO, 
HISTORICOS.ID_DE_PROCESO, 
HISTORICOS.ANO, 
HISTORICOS.MES, 
HISTORICOS.PERIODO, 
PERSONAL.ID_CENTRO_DE_COSTO, 
PERSONAL.FECHA_DE_INGRESO, 
PERSONAL.ID_DE_CARGO, 
CARGOS.ID_DE_CARGO,
PERSONAL.ID_DE_CARGOESCALERA, 
PERSONAL.ID_DE_CARGO_TEMPORAL, 
CARGOSE.NOMBRE_DEL_CARGO, 
CARGOST.NOMBRE_DEL_CARGO
HAVING PERSONAL.FONDO_CESANTIAS>='1' 
AND SUM(CASE WHEN  HISTORICOS.ID_DE_CONCEPTO='158' THEN HISTORICOS.VALOR ELSE 0 END)<>0
ORDER BY TRIM(APELLIDO1) || ' ' || TRIM(APELLIDO2) || ' ' || TRIM(NOMBRES)