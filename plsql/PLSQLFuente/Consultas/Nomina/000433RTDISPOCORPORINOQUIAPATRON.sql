SELECT 
    COMPANIA, 
    CODIGO_ESTABLECIMIENTO  CODIGO_ESTABLECIMIENTO, 
    NOMBRE_ESTABLECIMIENTO NOMBREESTABLECIMIENTO, 
    ID_CENTRO_DE_COSTO ID_CENTRO_DE_COSTO, 
    NOMBRE_CENTRO_DE_COSTO NOMBRE_CENTRO_DE_COSTO, 
    CLASE  CLASE, 
    ID_DE_CONCEPTO ID_DE_CONCEPTO, 
    CLASE_NOMBRE_MAYU CLASECONCEPTO, 
    NOMBRE_CONCEPTO  NOMBRE_CONCEPTO, 
    SUM(VALOR)  SUMA_DE_VALOR 
FROM V_ACUMULADOS    
WHERE COMPANIA = s$compania$s
AND CLASE = 8
AND ID_DE_CONCEPTO  NOT IN (107,  108, 112, 113, 115, 118, 493, 494, 495, 496, 497, 498, 499, 110, 134, 212) 
AND LPAD(ID_DE_PROCESO, 2, 0) || LPAD(ANO, 4, 0) || LPAD(MES, 2, 0) || LPAD(PERIODO, 2, 0) BETWEEN 's$fechaInicial$s'  AND 's$fechaFinal$s'  
GROUP BY 
     COMPANIA, 
    CODIGO_ESTABLECIMIENTO,
    NOMBRE_ESTABLECIMIENTO, 
    ID_CENTRO_DE_COSTO,
    NOMBRE_CENTRO_DE_COSTO, 
    CLASE,
    ID_DE_CONCEPTO, 
    CLASE_NOMBRE_MAYU, 
    NOMBRE_CONCEPTO
