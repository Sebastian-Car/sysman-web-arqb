SELECT
  PERSONAL.ID_DE_EMPLEADO,
  MAX(APELLIDO1 || ' ' || APELLIDO2 ||' '|| NOMBRES) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NOMCOMPLETO,
  PERSONAL.NUMERO_DCTO,
  MEDICINA_PREPAGADA.MEDICINA_PREPAGADA FONDO_PENSION_VOL,
  MAX(MEDICINA_PREPAGADA.NIT) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NIT,
  MAX(MEDICINA_PREPAGADA.NOMBRE_MEDICINA) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NOMBRE_DEL_FONDO,
  MAX(HISTORICOS.ID_DE_CONCEPTO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) PRIMERODEID_DE_CONCEPTO,
  SUM(VALOR) AVP,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES) NOMBRE_MES,
  PCK_SYSMAN_UTL.FC_PAR(UN_COMPANIA => HISTORICOS.COMPANIA, UN_NOMBRE => 'NOMBRE DEL GERENTE', UN_MODULO => 6, UN_FECHA_PAR => SYSDATE, UN_IND_MAYUS => -1) NOMBRE_GERENTE,
  PCK_SYSMAN_UTL.FC_PAR(UN_COMPANIA => HISTORICOS.COMPANIA, UN_NOMBRE => 'CARGO DEL GERENTE',  UN_MODULO => 6, UN_FECHA_PAR => SYSDATE, UN_IND_MAYUS => -1) CARGO_GERENTE,
  PCK_SYSMAN_UTL.FC_PAR(UN_COMPANIA => HISTORICOS.COMPANIA, UN_NOMBRE => 'NOMBRE DEL CARGO TESORERO PAGADOR', UN_MODULO => 6, UN_FECHA_PAR => SYSDATE, UN_IND_MAYUS => -1) NOMBRE_TESORERO,
  PCK_SYSMAN_UTL.FC_PAR(UN_COMPANIA => HISTORICOS.COMPANIA, UN_NOMBRE => 'CARGO DEL TESORERO PAGADOR', UN_MODULO => 6, UN_FECHA_PAR => SYSDATE, UN_IND_MAYUS => -1) CARGO_TESORERO,
  PCK_SYSMAN_UTL.FC_PAR(UN_COMPANIA => HISTORICOS.COMPANIA, UN_NOMBRE => 'NOMBRE DE QUIEN AUTORIZA NOMINA', UN_MODULO => 6, UN_FECHA_PAR => SYSDATE, UN_IND_MAYUS => -1) NOMBRE_AUTORIZA,
  PCK_SYSMAN_UTL.FC_PAR(UN_COMPANIA => HISTORICOS.COMPANIA, UN_NOMBRE => 'CARGO DE QUIEN AUTORIZA NOMINA', UN_MODULO => 6, UN_FECHA_PAR => SYSDATE, UN_IND_MAYUS => -1) CARGO_AUTORIZA
FROM HISTORICOS
INNER JOIN PERSONAL
	ON HISTORICOS.COMPANIA        = PERSONAL.COMPANIA
	AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
INNER JOIN V_MEDICINA_PREPAGADA MEDICINA_PREPAGADA
	ON PERSONAL.COMPANIA             = MEDICINA_PREPAGADA.COMPANIA
	AND PERSONAL.MEDICINA_PREPAGADA  = MEDICINA_PREPAGADA.MEDICINA_PREPAGADA
WHERE HISTORICOS.COMPANIA          = s$compania$s
	AND HISTORICOS.ID_DE_PROCESO   = s$procesoNomina$s
	AND HISTORICOS.ANO             = s$anioNomina$s
	AND HISTORICOS.MES             = s$mesNomina$s
	AND HISTORICOS.ID_DE_CONCEPTO  IN(100, 200,205)
	AND PERSONAL.ID_DE_EMPLEADO    NOT IN (0)
GROUP BY HISTORICOS.COMPANIA,
	 PERSONAL.ID_DE_EMPLEADO,
  	 PERSONAL.NUMERO_DCTO,
  	 MEDICINA_PREPAGADA.MEDICINA_PREPAGADA,
  	 HISTORICOS.ID_DE_PROCESO,
  	 HISTORICOS.ANO,
  	 HISTORICOS.MES
HAVING SUM(VALOR) > 0
