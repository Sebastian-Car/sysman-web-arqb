SELECT PERSONAL.COMPANIA, 
    ESTABLECIMIENTOS_DOCENTES.CODIGO AS ESTABLECIMIENTO, 
    ESTABLECIMIENTOS_DOCENTES.NOMBRE AS NOMBRE_ESTABLECIMIENTO, 
    PERSONAL.NOMBRECOMPLETO, 
    MAX(VACACIONES.ID_DE_PROCESO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS ID_DE_PROCESO, 
    MAX(HISTORICOS.ANO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS ANO, 
    MAX(HISTORICOS.MES) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS MES, 
    MIN(HISTORICOS.PERIODO)  KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) AS PERIODO,
    VACACIONES.ANO,
    VACACIONES.MES, 0 AS AUSENTISMO, 
    MAX(VACACIONES.FECHA_INICIO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM)  AS FECHA_INICIO, 
    MAX(VACACIONES.FECHA_FINAL) KEEP (DENSE_RANK LAST ORDER BY ROWNUM)  AS FECHA_FINAL, 
    MAX(VACACIONES.INICIO_DISFRUTE)  KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS INICIO_DISFRUTE, 
    MAX(VACACIONES.FINAL_DISFRUTE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM)  AS FINAL_DISFRUTE, 
    MAX(VACACIONES.DIAS) KEEP (DENSE_RANK LAST ORDER BY ROWNUM)  AS DIAS, 
    MAX(VACACIONES.FECHAPAGO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM)  AS FECHAPAGO, 
    PERSONAL.NUMERO_DCTO, 
    PERSONAL.FECHA_DE_INGRESO, 
    PERSONAL.ESTADO_ACTUAL, 
    TO_CHAR( CASE WHEN PERSONAL.FECHA_FINAL IS NULL THEN PERSONAL.FECHA_DE_INGRESO ELSE PERSONAL.FECHA_FINAL+1 END, 'DD/MM/YYYY') AS FIVP, 
    TO_CHAR(TO_DATE('s$fechaPeriodo$s'  , 'DD/MM/YYYY'), 'DD/MM/YYYY') AS FFVP, 
    TO_DATE('s$fechaPeriodo$s'  , 'DD/MM/YYYY') AS fechaperiodo, 
    Personal.ID_DE_EMPLEADO,
    TRUNC(  (TO_DATE('s$fechaPeriodo$s'  , 'DD/MM/YYYY')-TO_DATE(( CASE WHEN PERSONAL.FECHA_FINAL IS NULL THEN PERSONAL.FECHA_DE_INGRESO ELSE PERSONAL.FECHA_FINAL+1 END)+1)
        )/365)*15 AS periodos, 
    PERSONAL.DEPENDENCIA, 
    CARGOS.NOMBRE_DEL_CARGO  || ' '  ||  CATEGORIA.CODIGOGRADO  AS NOMBRE_DEL_CARGO, 
    CATEGORIA.CODIGOGRADO, 
    PERSONAL.FECHAINIPROGVAC, 
    PERSONAL.FECHAFINPROGVAC, 
    VACACIONES.ENDINERO,
    VACACIONES.DIASHABILES, 
    PERSONAL.NUMERO_DCTO
FROM ((HISTORICOS INNER JOIN ((PERSONAL LEFT JOIN CARGOS ON 
            PERSONAL.COMPANIA = CARGOS.COMPANIA 
            AND PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO) 
        INNER JOIN CATEGORIA ON PERSONAL.COMPANIA = CATEGORIA.COMPANIA 
            AND PERSONAL.ANO = CATEGORIA.ANO 
            AND PERSONAL.ID_DE_CATEGORIA = CATEGORIA.ID_DE_CATEGORIA 
            AND PERSONAL.ESCALAFON = CATEGORIA.ESCALAFON 
        ) ON HISTORICOS.COMPANIA = PERSONAL.COMPANIA 
        AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO ) 
	INNER JOIN VACACIONES ON HISTORICOS.COMPANIA = VACACIONES.COMPANIA 
      AND HISTORICOS.ID_DE_PROCESO = VACACIONES.ID_DE_PROCESO 
      AND HISTORICOS.ANO = VACACIONES.ANO 
      AND HISTORICOS.MES = VACACIONES.MES 
      AND HISTORICOS.ID_DE_EMPLEADO = VACACIONES.ID_DE_EMPLEADO) 
	LEFT JOIN ESTABLECIMIENTOS_DOCENTES ON 
  PERSONAL.COMPANIA = ESTABLECIMIENTOS_DOCENTES.COMPANIA
  AND PERSONAL.CODIGO_ESTABLECIMIENTO = ESTABLECIMIENTOS_DOCENTES.CODIGO 
WHERE 
	PERSONAL.COMPANIA= s$compania$s 
	AND HISTORICOS.ID_DE_CONCEPTO IN ('155','151','174','175','099','140','960') 
	AND HISTORICOS.ANO= s$anoFormulario$s
	AND HISTORICOS.MES= s$mesFormulario$s
	AND PERSONAL.ESTADO_ACTUAL<>3 
	AND PERSONAL.ESTADO_ACTUAL<>2 
	AND PERSONAL.ID_DE_EMPLEADO<>0
	AND VACACIONES.ID_DE_PROCESO  =  s$procesoSesion$s
GROUP BY 
	PERSONAL.COMPANIA, 
	ESTABLECIMIENTOS_DOCENTES.CODIGO, 
	ESTABLECIMIENTOS_DOCENTES.NOMBRE, 
	PERSONAL.NOMBRECOMPLETO, 
	VACACIONES.ANO,
	VACACIONES.MES, 
	0,
	PERSONAL.FECHA_DE_INGRESO, 
	PERSONAL.ESTADO_ACTUAL, 
	( CASE WHEN PERSONAL.FECHA_FINAL IS NULL THEN PERSONAL.FECHA_DE_INGRESO ELSE PERSONAL.FECHA_FINAL+1 END), 
	TO_DATE('s$fechaPeriodo$s'  , 'DD/MM/YYYY'), 
	PERSONAL.ID_DE_EMPLEADO, 
	PERSONAL.DEPENDENCIA, 
	CARGOS.NOMBRE_DEL_CARGO  || ' '  ||  CATEGORIA.CODIGOGRADO , 
	CATEGORIA.CODIGOGRADO, 
	PERSONAL.FECHAINIPROGVAC, 
	PERSONAL.FECHAFINPROGVAC,
	VACACIONES.ENDINERO,
	VACACIONES.DIASHABILES,
	PERSONAL.NUMERO_DCTO, 
  TO_DATE('s$fechaPeriodo$s'  , 'DD/MM/YYYY')
ORDER BY PERSONAL.NOMBRECOMPLETO
