SELECT  
    ESTABLECIMIENTOS_DOCENTES.CODIGO AS ESTABLECIMIENTO, 
    ESTABLECIMIENTOS_DOCENTES.NOMBRE AS NOMBRE_ESTABLECIMIENTO,
    PERSONAL.NOMBRECOMPLETO NOM_EMPLEADO,
    HISTORICOS.ID_DE_EMPLEADO AS CODIGO,
    CENTRO_COSTO.CODIGO ID_CENTRO_DE_COSTO,
    CENTRO_COSTO.NOMBRE NOMBRE_CENTRO_DE_COSTO,
    CATEGORIA.SALARIO_BASE,
    CATEGORIA.CODIGOGRADO,
    MAX(HISTORICOS.ID_DE_PROCESO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) PROCESO,
    MAX(HISTORICOS.ANO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) ANO,
    MAX(HISTORICOS.MES) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) MES,
    MAX(PERSONAL.ID_DE_CARGO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) CARGO,        
    MAX(CARGOS.NOMBRE_DEL_CARGO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) NOMBRECARGO,                
    MAX(PERSONAL.DEPENDENCIA) DEPENDENCIA,
    MAX(DEPENDENCIA.NOMBRE) NOMBREDEPENDENCIA,
    MAX(HISTORICOS.PERIODO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) PERIODO,
    MAX(HISTORICOS.COMPANIA) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) COMPANIA,
    MAX(PERSONAL.NUMERO_DCTO) CEDULA,         
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('144') THEN HISTORICOS.VALOR ELSE 0 END) C144,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('157') THEN HISTORICOS.VALOR ELSE 0 END) C157,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('449') THEN HISTORICOS.VALOR ELSE 0 END) C449,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('129') THEN HISTORICOS.VALOR ELSE 0 END + 
    CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('635') THEN HISTORICOS.VALOR ELSE 0 END) SINDICATO,
    SUM(CASE WHEN CONCEPTOS.CLASE IN (3) THEN HISTORICOS.VALOR  ELSE 0 END) DEVENGOS,
    SUM(CASE WHEN CONCEPTOS.CLASE IN (5) THEN HISTORICOS.VALOR  ELSE 0 END) AS DESCUENTOS,  
    SUM(CASE WHEN CONCEPTOS.CLASE IN (3) THEN HISTORICOS.VALOR  ELSE 0 END - 
        CASE WHEN CONCEPTOS.CLASE IN (5) THEN HISTORICOS.VALOR  ELSE 0 END)NETO,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('699') THEN HISTORICOS.VALOR ELSE 0 END -  
        CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('637') THEN HISTORICOS.VALOR ELSE 0 END - 
        CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('635') THEN HISTORICOS.VALOR ELSE 0 END +
        CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('124') THEN HISTORICOS.VALOR ELSE 0 END +
        CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('127') THEN HISTORICOS.VALOR ELSE 0 END +
        CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('120') THEN HISTORICOS.VALOR ELSE 0 END +
        CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('123') THEN HISTORICOS.VALOR ELSE 0 END +
        CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('224') THEN HISTORICOS.VALOR ELSE 0 END)TOTALDSTOS,
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('637') THEN HISTORICOS.VALOR ELSE 0 END +
        CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('635') THEN HISTORICOS.VALOR ELSE 0 END) ESTAMPILLAS,
    MAX(PERSONAL.FECHA_DE_INGRESO) FECHA_DE_INGRESO,
    MAX(PERSONAL.FECHA_DE_RETIRO) FECHA_DE_RETIRO                 
FROM HISTORICOS 
  INNER JOIN CONCEPTOS 
    ON HISTORICOS.COMPANIA        = CONCEPTOS.COMPANIA  
    AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO     
  INNER JOIN PERIODOS
    ON HISTORICOS.COMPANIA        = PERIODOS.COMPANIA
    AND HISTORICOS.ID_DE_PROCESO  = PERIODOS.ID_DE_PROCESO
    AND HISTORICOS.ANO            = PERIODOS.ANO
    AND HISTORICOS.MES            = PERIODOS.MES
    AND HISTORICOS.PERIODO        = PERIODOS.PERIODO 
  INNER JOIN PERSONAL 
    ON HISTORICOS.COMPANIA        = PERSONAL.COMPANIA  
    AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO   
  INNER JOIN DEPENDENCIA
    ON PERSONAL.COMPANIA     = DEPENDENCIA.COMPANIA
    AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
  INNER JOIN CARGOS
    ON PERSONAL.COMPANIA    = CARGOS.COMPANIA
   AND PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO 
  INNER JOIN CENTRO_COSTO
    ON PERSONAL.COMPANIA          	= CENTRO_COSTO.COMPANIA	
   AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO 
   AND HISTORICOS.ANO              = CENTRO_COSTO.ANO  
  INNER JOIN CATEGORIA
     ON PERSONAL.COMPANIA        = CATEGORIA.COMPANIA
    AND PERSONAL.ESCALAFON       = CATEGORIA.ESCALAFON
    AND PERSONAL.ID_DE_CATEGORIA = CATEGORIA.ID_DE_CATEGORIA
    AND PERSONAL.ANO             = CATEGORIA.ANO    
  LEFT JOIN ESTABLECIMIENTOS_DOCENTES 
    ON PERSONAL.COMPANIA 				        = ESTABLECIMIENTOS_DOCENTES.COMPANIA 
   AND PERSONAL.CODIGO_ESTABLECIMIENTO = ESTABLECIMIENTOS_DOCENTES.CODIGO 
WHERE HISTORICOS.COMPANIA		    =	s$compania$s
  AND HISTORICOS.ID_DE_PROCESO	                         =	s$proceso$s
  AND HISTORICOS.ANO 			    =	s$ano$s
  AND HISTORICOS.MES 			    =	s$mes$s
  AND HISTORICOS.PERIODO		    =	s$periodo$s 
  AND PERIODOS.ACUMULADO NOT IN(0) 
GROUP BY  ESTABLECIMIENTOS_DOCENTES.CODIGO ,
          ESTABLECIMIENTOS_DOCENTES.NOMBRE ,
          PERSONAL.NOMBRECOMPLETO,
          HISTORICOS.ID_DE_EMPLEADO,
          CENTRO_COSTO.CODIGO,
          CENTRO_COSTO.NOMBRE,
          PERSONAL.ID_DE_EMPLEADO,
          CATEGORIA.SALARIO_BASE,
          CATEGORIA.CODIGOGRADO
ORDER BY ESTABLECIMIENTOS_DOCENTES.CODIGO
