SELECT PERSONAL.FONDO_RIESGOS AS FONDO_RIESGOS, 
    PERSONAL.NUMERO_DCTO AS CEDULA, 
    PERSONAL.ID_CENTRO_DE_COSTO AS ID_CENTRO_DE_COSTO, 
    V_FONDO_DE_RIESGOS.NOMBRE_FONDO_RIESGOS,
    CENTRO_COSTO.NOMBRE NOMBRE_CENTRO_DE_COSTO,
    MIN(PERSONAL.SALARIO_BASE_IBC) KEEP (DENSE_RANK FIRST ORDER BY PERSONAL.NUMERO_DCTO)SALARIO_BASE_IBC,
    UPPER(PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES))  NOMBREMES,
    HISTORICOS.ID_DE_PROCESO AS PROCESO, 
    HISTORICOS.ANO AS ANO, 
    HISTORICOS.MES AS MES,
    PERSONAL.ID_DE_CARGO AS CARGO,
    PERSONAL.DEPENDENCIA AS DEPENDENCIA, 
    DEPENDENCIA.NOMBRE AS NOMBREDEPENDENCIA, 
    HISTORICOS.PERIODO AS PERIODO, 
    PERSONAL.APELLIDO1 || ' ' || PERSONAL.APELLIDO2 || ' ' || PERSONAL.NOMBRES AS NOM_EMPLEADO,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '109' THEN HISTORICOS.VALOR ELSE 0 END) AS C109,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '112' THEN HISTORICOS.VALOR ELSE 0 END) AS C112,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '209' THEN HISTORICOS.VALOR ELSE 0 END) AS C209,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '212' THEN HISTORICOS.VALOR ELSE 0 END) AS C212,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '397' THEN HISTORICOS.VALOR ELSE 0 END) AS C397,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '111' THEN HISTORICOS.VALOR ELSE 0 END) AS C111,
    HISTORICOS.COMPANIA, 
    PATRONALES.FACTOR AS PORRIESGO, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '111' THEN HISTORICOS.VALOR ELSE 0 END)-SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '397' THEN HISTORICOS.VALOR ELSE 0 END) AS TOTAL
FROM HISTORICOS
LEFT JOIN PERIODOS 
    ON HISTORICOS.COMPANIA = PERIODOS.COMPANIA 
    AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO
    AND HISTORICOS.ANO = PERIODOS.ANO 
    AND HISTORICOS.MES = PERIODOS.MES
    AND HISTORICOS.PERIODO = PERIODOS.PERIODO
LEFT JOIN PERSONAL 
    ON HISTORICOS.COMPANIA = PERSONAL.COMPANIA
    AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
LEFT JOIN PATRONALES 
    ON PERSONAL.NUMEROPATRONAL = PATRONALES.SUCURSAL
    AND PERSONAL.COMPANIA = PATRONALES.COMPANIA
LEFT JOIN CENTRO_COSTO
    ON PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO 
    AND PERSONAL.COMPANIA = CENTRO_COSTO.COMPANIA
    AND HISTORICOS.ANO = CENTRO_COSTO.ANO
LEFT JOIN DEPENDENCIA
    ON PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
    AND PERSONAL.COMPANIA = DEPENDENCIA.COMPANIA
LEFT JOIN V_FONDO_DE_RIESGOS 
    ON PERSONAL.FONDO_RIESGOS = V_FONDO_DE_RIESGOS.FONDO_RIESGOS 
    AND PERSONAL.COMPANIA = V_FONDO_DE_RIESGOS.COMPANIA
WHERE PERIODOS.ACUMULADO <>0 
AND HISTORICOS.ID_DE_PROCESO = s$procesoNomina$s
AND HISTORICOS.ANO = s$anioNomina$s
AND HISTORICOS.MES = s$mesNomina$s 
AND HISTORICOS.COMPANIA =s$compania$s 
AND HISTORICOS.PERIODO =s$periodoNomina$s
AND HISTORICOS.ID_DE_CONCEPTO IN (112,209,111,397,212,109)
GROUP BY PERSONAL.FONDO_RIESGOS, 
    PERSONAL.ID_CENTRO_DE_COSTO, 
    PERSONAL.NUMERO_DCTO,
    V_FONDO_DE_RIESGOS.NOMBRE_FONDO_RIESGOS, 
    CENTRO_COSTO.NOMBRE, 
    PERSONAL.COMPANIA,
    PATRONALES.FACTOR, 
    HISTORICOS.ID_DE_EMPLEADO,
    HISTORICOS.ID_DE_PROCESO,
    HISTORICOS.ANO, 
    HISTORICOS.MES, 
    PERSONAL.ID_DE_CARGO, 
    PERSONAL.DEPENDENCIA, 
    DEPENDENCIA.NOMBRE, 
    HISTORICOS.PERIODO, 
    PERSONAL.APELLIDO1 || ' ' || PERSONAL.APELLIDO2 || ' ' || PERSONAL.NOMBRES,
    HISTORICOS.COMPANIA
ORDER BY PERSONAL.FONDO_RIESGOS,
    PERSONAL.NUMERO_DCTO