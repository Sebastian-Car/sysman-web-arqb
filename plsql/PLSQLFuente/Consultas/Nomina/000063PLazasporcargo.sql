SELECT PERSONAL.COMPANIA,  
       MIN(PERSONAL.ESCALAFON) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) ESCALAFON1,  
       CARGOS.ESCALAFON,  
       CARGOS.NOMBRE_DEL_CARGO,  
       CATEGORIA.CODIGOGRADO CODIGOO,  
       CARGOS.GRADO,  
       COUNT(CARGOS.ID_DE_CARGO) CARGOS,  
       CARGOS.PLAZAS,  
       (CARGOS.PLAZAS)-(COUNT(CARGOS.ID_DE_CARGO)) DIFER,  
       MIN(CATEGORIA.SALARIO_BASE) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) BASE,  
       (MIN(CATEGORIA.SALARIO_BASE) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM))*((CARGOS.PLAZAS)-(COUNT(CARGOS.ID_DE_CARGO))) TOTAL,  
       ESCALAFON.NOMBRE  NOMBREESCALAFON 
FROM PERSONAL  
LEFT JOIN CATEGORIA  
    ON  PERSONAL.COMPANIA          = CATEGORIA.COMPANIA 
    AND PERSONAL.ANO               = CATEGORIA.ANO  
    AND PERSONAL.ID_DE_CATEGORIA   = CATEGORIA.ID_DE_CATEGORIA  
    AND PERSONAL.ESCALAFON         = CATEGORIA.ESCALAFON  
LEFT JOIN CARGOS  
    ON  PERSONAL.COMPANIA      = CARGOS.COMPANIA 
    AND PERSONAL.ID_DE_CARGO   = CARGOS.ID_DE_CARGO  
INNER JOIN ESCALAFON  
    ON  PERSONAL.COMPANIA   = ESCALAFON.COMPANIA  
    AND PERSONAL.ESCALAFON  = ESCALAFON.CODIGO  
WHERE PERSONAL.COMPANIA       = s$compania$s 
  AND PERSONAL.ESTADO_ACTUAL  = 1 
  AND CARGOS.ESTADOCARGO = 1 
  AND CATEGORIA.ANO           = s$anioNomina$s 
GROUP BY PERSONAL.COMPANIA,  
              CARGOS.ESCALAFON,  
              CARGOS.NOMBRE_DEL_CARGO, 
              CATEGORIA.CODIGOGRADO,  
              CARGOS.GRADO,  
              CARGOS.PLAZAS,  
              ESCALAFON.NOMBRE 
ORDER BY CARGOS.ESCALAFON