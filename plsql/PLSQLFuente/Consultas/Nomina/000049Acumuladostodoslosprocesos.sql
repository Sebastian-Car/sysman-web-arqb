SELECT V_ACUMULADOS.NOMBRECOMPLETO,
  V_ACUMULADOS.CLASE,
  V_ACUMULADOS.ID_DE_CONCEPTO,
  MAX(V_ACUMULADOS.ID_DE_EMPLEADO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS ID_DE_EMPLEADO, 
  V_ACUMULADOS.NUMERO_DCTO,
  SUM(V_ACUMULADOS.VALOR) AS SUMA_DE_VALOR,
  V_ACUMULADOS.NOMBRE_CONCEPTO,
  SUM(V_ACUMULADOS.NETOAPAGAR) AS DESCONCEPTO,
  V_ACUMULADOS.CLASE_NOMBRE 
FROM V_ACUMULADOS 
WHERE V_ACUMULADOS.COMPANIA =s$compania$s 
  AND V_ACUMULADOS.ANO || LPAD(V_ACUMULADOS.MES,2,'0') || LPAD(V_ACUMULADOS.PERIODO,2,'0') 
  BETWEEN s$DESDE$s 
  AND s$HASTA$s
  s$CONDEMPLEADO$s 
  s$CONDCLASE$s 
GROUP BY V_ACUMULADOS.NOMBRECOMPLETO,
  V_ACUMULADOS.CLASE,
  V_ACUMULADOS.ID_DE_CONCEPTO,
  V_ACUMULADOS.NOMBRE_CONCEPTO,
  V_ACUMULADOS.NUMERO_DCTO,
  V_ACUMULADOS.CLASE_NOMBRE 
ORDER BY 
  V_ACUMULADOS.CLASE,
  V_ACUMULADOS.ID_DE_CONCEPTO
