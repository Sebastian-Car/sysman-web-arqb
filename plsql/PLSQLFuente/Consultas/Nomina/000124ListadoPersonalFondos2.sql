SELECT DISTINCT 
        PERSONAL.NOMBRECOMPLETO AS NOMCOMPLETO, 
        PERSONAL.ID_DE_EMPLEADO, 
        PERSONAL.ESTADO_ACTUAL, 
        PERSONAL.FONDO_CESANTIAS, 
        PERSONAL.FECHAFONDOCESANTIA AS FECHAFONDO,
        V_FONDO_DE_CESANTIAS.NIT,
        V_FONDO_DE_CESANTIAS.NOMBRE_FONDO_CESANTIAS AS NOMBREFONDO, 
        V_FONDO_DE_CESANTIAS.FONDO_CESANTIAS AS IDFONDO, 
        PERSONAL.NUMERO_DCTO
   FROM PERSONAL 
   LEFT JOIN V_FONDO_DE_CESANTIAS ON 
   PERSONAL.COMPANIA = V_FONDO_DE_CESANTIAS.COMPANIA AND 
   PERSONAL.FONDO_CESANTIAS = V_FONDO_DE_CESANTIAS.FONDO_CESANTIAS
   WHERE PERSONAL.ID_DE_EMPLEADO NOT IN (0)
     AND PERSONAL.ESTADO_ACTUAL NOT IN (3)
     AND PERSONAL.COMPANIA= s$compania$s
