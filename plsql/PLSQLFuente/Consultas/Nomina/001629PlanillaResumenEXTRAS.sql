SELECT 
  HISTORICOS.COMPANIA,
  HISTORICOS.ID_DE_EMPLEADO AS CODIGO,
  CENTRO_COSTO.CODIGO ID_CENTRO_DE_COSTO,
  CENTRO_COSTO.NOMBRE NOMBRE_CENTRO_DE_COSTO,
  CATEGORIA.SALARIO_BASE AS SALARIO_BASE,
  CATEGORIA.CODIGOGRADO  AS CODIGOGRADO,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  SUM(HISTORICOS.VALOR) AS SUMADEVALOR,
  PERSONAL.NOMBRECOMPLETO  AS NOM_EMPLEADO,
  PERSONAL.NUMERO_DCTO AS CEDULA,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('001') THEN HISTORICOS.VALOR ELSE 0 END) AS C001,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('070') THEN HISTORICOS.VALOR ELSE 0 END) AS C070,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('018') THEN HISTORICOS.VALOR ELSE 0 END) AS C018,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('056') THEN HISTORICOS.VALOR ELSE 0 END) AS C056,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('017') THEN HISTORICOS.VALOR ELSE 0 END) AS C017,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('050') THEN HISTORICOS.VALOR ELSE 0 END) AS C050,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('019') THEN HISTORICOS.VALOR ELSE 0 END) AS C019,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('051') THEN HISTORICOS.VALOR ELSE 0 END) AS C051,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('020') THEN HISTORICOS.VALOR ELSE 0 END) AS C020,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('059') THEN HISTORICOS.VALOR ELSE 0 END) AS C059,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('026') THEN HISTORICOS.VALOR ELSE 0 END) AS C026,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('047') THEN HISTORICOS.VALOR ELSE 0 END) AS C047,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('021') THEN HISTORICOS.VALOR ELSE 0 END) AS C021,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('060') THEN HISTORICOS.VALOR ELSE 0 END) AS C060,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('031') THEN HISTORICOS.VALOR ELSE 0 END) AS C031,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('045') THEN HISTORICOS.VALOR ELSE 0 END) AS C045,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('027') THEN HISTORICOS.VALOR ELSE 0 END) AS C027,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('057') THEN HISTORICOS.VALOR ELSE 0 END) AS C057,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('009') THEN HISTORICOS.VALOR ELSE 0 END) AS C009,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('144') THEN HISTORICOS.VALOR ELSE 0 END) AS C144,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('028') THEN HISTORICOS.VALOR ELSE 0 END) AS C028,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('046') THEN HISTORICOS.VALOR ELSE 0 END) AS C046,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('029') THEN HISTORICOS.VALOR ELSE 0 END) AS C029
FROM HISTORICOS
  INNER JOIN CONCEPTOS
    ON HISTORICOS.COMPANIA        = CONCEPTOS.COMPANIA 
    AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
  INNER JOIN PERIODOS
    ON HISTORICOS.COMPANIA        = PERIODOS.COMPANIA
    AND HISTORICOS.ID_DE_PROCESO  = PERIODOS.ID_DE_PROCESO
    AND HISTORICOS.ANO            = PERIODOS.ANO
    AND HISTORICOS.MES            = PERIODOS.MES
    AND HISTORICOS.PERIODO        = PERIODOS.PERIODO 
  INNER JOIN PERSONAL 
    ON HISTORICOS.COMPANIA          = PERSONAL.COMPANIA  
    AND HISTORICOS.ID_DE_EMPLEADO   = PERSONAL.ID_DE_EMPLEADO 
  LEFT JOIN CENTRO_COSTO
    ON PERSONAL.COMPANIA            = CENTRO_COSTO.COMPANIA
    AND PERSONAL.ANO                = CENTRO_COSTO.ANO
    AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
  LEFT JOIN CATEGORIA
    ON PERSONAL.COMPANIA          = CATEGORIA.COMPANIA 
    AND PERSONAL.ESCALAFON        = CATEGORIA.ESCALAFON
    AND PERSONAL.ID_DE_CATEGORIA  = CATEGORIA.ID_DE_CATEGORIA
    AND PERSONAL.ANO              = CATEGORIA.ANO
WHERE HISTORICOS.COMPANIA		=	s$compania$s
  AND HISTORICOS.ID_DE_PROCESO	=	s$proceso$s 
  AND HISTORICOS.ANO 			=	s$ano$s
  AND HISTORICOS.MES 			=	s$mes$s
  AND HISTORICOS.PERIODO		=	s$periodo$s 
  AND PERIODOS.ACUMULADO NOT IN(0)
  AND (HISTORICOS.ID_DE_CONCEPTO BETWEEN '016' AND '060' OR HISTORICOS.ID_DE_CONCEPTO IN('070','009','001'))
GROUP BY  
  HISTORICOS.COMPANIA,
  HISTORICOS.ID_DE_EMPLEADO,
  CENTRO_COSTO.CODIGO,
  CENTRO_COSTO.NOMBRE,
  CATEGORIA.SALARIO_BASE,
  CATEGORIA.CODIGOGRADO ,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  PERSONAL.NOMBRECOMPLETO,
  PERSONAL.NUMERO_DCTO
HAVING SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('070') THEN HISTORICOS.VALOR ELSE 0 END)>0
