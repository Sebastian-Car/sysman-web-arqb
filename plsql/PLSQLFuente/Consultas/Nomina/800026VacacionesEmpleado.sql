SELECT   
    PERSONAL.COMPANIA,   
    PERSONAL.NUMERO_DCTO,   
    PERSONAL.APELLIDO1,   
    PERSONAL.APELLIDO2,   
    PERSONAL.NOMBRES,  
    VACACIONES.ID_DE_EMPLEADO CODIGO_EMPLEADO,   
    VACACIONES.ID_DE_PROCESO,   
    VACACIONES.ANO,   
    VACACIONES.MES,   
    VACACIONES.PERIODO,   
    VACACIONES.ID_DE_CONCEPTO CODIGO_CONCEPTO,   
    (SELECT NOMBRE_CONCEPTO FROM CONCEPTOS WHERE COMPANIA = VACACIONES.COMPANIA AND ID_DE_CONCEPTO = VACACIONES.ID_DE_CONCEPTO) NOMBRE_CONCEPTO,   
    TO_CHAR(VACACIONES.FECHA_INICIO, 'DD/MM/YYYY') FECHA_INICIO,   
    TO_CHAR(VACACIONES.FECHA_FINAL, 'DD/MM/YYYY') FECHA_FINAL,   
    TO_CHAR(VACACIONES.INICIO_DISFRUTE,  'DD/MM/YYYY') FECHA_INICIO_DISFRUTE,   
    TO_CHAR(VACACIONES.FINAL_DISFRUTE,  'DD/MM/YYYY') FINAL_DISFRUTE,   
    VACACIONES.ACTO,   
    TO_CHAR(VACACIONES.FECHA_ACTO, 'DD/MM/YYYY') FECHA_ACTO,   
    VACACIONES.DIAS,   
    VACACIONES.ENDINERO,   
    TO_CHAR(VACACIONES.FECHAPAGO, 'DD/MM/YYYY') FECHA_PAGO,   
    VACACIONES.DIASHABILES,   
    VACACIONES.NUMPERIODOS,   
    VACACIONES.DIASDINERO,   
    VACACIONES.DIASPENDIENTESDEDISFRUTE   
   FROM   
    VACACIONES   
    INNER JOIN PERSONAL ON (VACACIONES.COMPANIA = PERSONAL.COMPANIA) AND (VACACIONES.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO)   
  WHERE   
      VACACIONES.COMPANIA = s$compania$s   
    AND VACACIONES.ID_DE_EMPLEADO =   s$idEmpleado$s     
    AND PERSONAL.NUMERO_DCTO IS NOT NULL   
  ORDER BY VACACIONES.ANO, VACACIONES.MES, VACACIONES.PERIODO, VACACIONES.FECHA_INICIO
