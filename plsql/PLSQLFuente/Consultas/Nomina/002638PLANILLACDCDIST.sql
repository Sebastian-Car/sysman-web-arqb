) PIVOT ( SUM(VALOR) FOR ID_DE_CONCEPTO IN (2 AS "C002",182 AS "C182",158 AS "C158",80 AS "C080",160 AS "C160",130 AS "C130",97 AS "C097",155 AS "C155", 70 AS "C070" , 144 AS "C144" , 174 AS "C174",131 AS "C131", 132 AS "C132") ) 
GROUP BY ID_CENTRO_DE_COSTO, GRUPO, NOMBRE_CENTRO_DE_COSTO, GRUPO1, PROCESO, 
ANO, MES, DEPENDENCIA, NOMBREDEPENDENCIA, PERIODO]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);