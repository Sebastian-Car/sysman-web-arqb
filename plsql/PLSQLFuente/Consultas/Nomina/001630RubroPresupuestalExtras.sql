SELECT 
  CASE PERSONAL.GRUPOCONTABLE 
    WHEN 'A'
    THEN 'ADMINISTRACION'
    WHEN 'V'
    THEN 'OPERACION'
    WHEN 'P'
    THEN 'PRODUCCION/PENSIONADO'
    ELSE 'OTRO' END  GRUPOCONTABLE,
  HISTORICOS.ID_DE_CONCEPTO,
  CONCEPTOS.NOMBRE_CONCEPTO,
  MIN(CASE WHEN PERSONAL.GRUPOCONTABLE IN('A') THEN CONCEPTOS.CTA_CRE_PPTAL ELSE CONCEPTOS.CTA_CONTRACRE_PPTAL END) AS RUBRO,
  SUM(HISTORICOS.VALOR)   AS TOTAL
FROM HISTORICOS
  INNER JOIN CONCEPTOS 
    ON HISTORICOS.COMPANIA        = CONCEPTOS.COMPANIA  
    AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO 
  INNER JOIN PERSONAL 
    ON HISTORICOS.COMPANIA        = PERSONAL.COMPANIA  
    AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO 
  LEFT JOIN TIPOS_DE_EMPLEADO
    ON PERSONAL.COMPANIA         = TIPOS_DE_EMPLEADO.COMPANIA
    AND PERSONAL.ID_DE_TIPO      = TIPOS_DE_EMPLEADO.ID_DE_TIPO
WHERE HISTORICOS.COMPANIA       = $P!{PR_COMPANIA}
  AND HISTORICOS.ID_DE_PROCESO  = $P!{PR_ID_DE_PROCESO}
  AND HISTORICOS.ANO            = $P!{PR_ANO}
  AND HISTORICOS.MES            = $P!{PR_MES}
  AND HISTORICOS.PERIODO        = $P!{PR_PERIODO}
  AND CONCEPTOS.CLASE IN(3)
  AND HISTORICOS.ID_DE_CONCEPTO BETWEEN '045' AND '060'
GROUP BY  CASE PERSONAL.GRUPOCONTABLE 
    WHEN 'A'
    THEN 'ADMINISTRACION'
    WHEN 'V'
    THEN 'OPERACION'
    WHEN 'P'
    THEN 'PRODUCCION/PENSIONADO'
    ELSE 'OTRO' END,
    HISTORICOS.ID_DE_CONCEPTO,
    CONCEPTOS.NOMBRE_CONCEPTO
