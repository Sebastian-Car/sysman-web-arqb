SELECT CONCEPTOS.ID_DE_CONCEPTO,  
       CONCEPTOS.NOMBRE_CONCEPTO,  
       CASE CONCEPTOS.FACTOR_PARAFISCAL WHEN 0 THEN ' ' WHEN -1  THEN 'X' END FACTOR_PARAFISCAL  
FROM CONCEPTOS  
WHERE CONCEPTOS.COMPANIA             =  s$compania$s 
  AND CONCEPTOS.FACTOR_PARAFISCAL    NOT IN(0) 
ORDER BY ID_DE_CONCEPTO
