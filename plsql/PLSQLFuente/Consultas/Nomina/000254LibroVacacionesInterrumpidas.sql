WITH 
 LIBRO_INTERRUPCION_HIS_091 AS 
  (SELECT HISTORICOS.COMPANIA,
          HISTORICOS.ID_DE_PROCESO,
          HISTORICOS.ID_DE_EMPLEADO,
          HISTORICOS.FECHA,
          HISTORICOS.VALOR
   FROM HISTORICOS
   WHERE HISTORICOS.COMPANIA      = s$compania$s
     AND HISTORICOS.ID_DE_PROCESO = s$procesoNomina$s
     AND HISTORICOS.ID_DE_CONCEPTO= 91
  ), 
 LIBRO_INTERRUPCION_HIS_099 AS
  (SELECT HISTORICOS.COMPANIA,
          HISTORICOS.ID_DE_PROCESO,
          HISTORICOS.FECHA,
          HISTORICOS.ANO,
          HISTORICOS.MES,
          HISTORICOS.PERIODO,
          HISTORICOS.ID_DE_EMPLEADO,
          HISTORICOS.ID_DE_CONCEPTO,
          HISTORICOS.VALOR
   FROM HISTORICOS
   WHERE HISTORICOS.COMPANIA        = s$compania$s
     AND HISTORICOS.ID_DE_PROCESO   = s$procesoNomina$s
     AND HISTORICOS.ID_DE_CONCEPTO  = 99
  ),
 LIBRO_INTERRUPCION_VAC_INI AS 
  (SELECT VACACIONES.COMPANIA,
          VACACIONES.ID_DE_PROCESO,
          VACACIONES.PERIODO,
          VACACIONES.ID_DE_EMPLEADO,
          VACACIONES.ID_DE_CONCEPTO,
          VACACIONES.DIASPENDIENTESDEDISFRUTE,
          VACACIONES.FECHA_INICIO,
          VACACIONES.FECHA_FINAL,
          VACACIONES.INICIO_DISFRUTE,
          VACACIONES.FINAL_DISFRUTE,
          VACACIONES.FECHAPAGO,
          VACACIONES.ENDINERO EN_DIN
   FROM VACACIONES
   WHERE VACACIONES.COMPANIA                  = s$compania$s
     AND VACACIONES.ID_DE_PROCESO             = s$procesoNomina$s
     AND VACACIONES.DIASPENDIENTESDEDISFRUTE  <=0
     AND ENDINERO                             = 0
  ),     
 LIBRO_INTERRUPCION_VAC_FIN AS
  (SELECT VACACIONES.COMPANIA,
          VACACIONES.ID_DE_PROCESO,
          VACACIONES.ANO,
          VACACIONES.MES,
          VACACIONES.PERIODO,
          VACACIONES.ID_DE_EMPLEADO,
          VACACIONES.ID_DE_CONCEPTO,
          VACACIONES.DIASPENDIENTESDEDISFRUTE DISFRUTADOS,
          VACACIONES.FECHA_INICIO INI_PER,
          VACACIONES.FECHA_FINAL FIN_PER,
          VACACIONES.INICIO_DISFRUTE INI_DIS,
          VACACIONES.FINAL_DISFRUTE FIN_DIS,
          VACACIONES.FECHAPAGO FECHA_RENOVACION
   FROM VACACIONES
   WHERE VACACIONES.COMPANIA       = s$compania$s
     AND VACACIONES.ID_DE_PROCESO  = s$procesoNomina$s
     AND VACACIONES.DIASPENDIENTESDEDISFRUTE>0
  )
SELECT PERSONAL.ID_DE_EMPLEADO, 
      PERSONAL.NOMBRECOMPLETO  NOMCOMPLETO, 
      INTERRUPCION_VACACIONES.DIASINTERRUPCION  INTERRUMPIDO, 
      INTERRUPCION_VACACIONES.FECHAINTERRUPCION, 
      INTERRUPCION_VACACIONES.INTERRUPCION_ENDINERO,
      LIBRO_INTERRUPCION_VAC_FIN.DISFRUTADOS, 
      LIBRO_INTERRUPCION_VAC_INI.FECHA_INICIO  I_PER_INI, 
      LIBRO_INTERRUPCION_VAC_INI.FECHA_FINAL  I_PER_FIN, 
      LIBRO_INTERRUPCION_VAC_INI.INICIO_DISFRUTE  I_DIS_INI, 
      LIBRO_INTERRUPCION_VAC_INI.FINAL_DISFRUTE  I_DIS_FIN, 
      LIBRO_INTERRUPCION_VAC_FIN.FIN_DIS, 
      LIBRO_INTERRUPCION_VAC_FIN.INI_DIS, 
      LIBRO_INTERRUPCION_HIS_099.VALOR C99,
      LIBRO_INTERRUPCION_HIS_091.VALOR C91
FROM INTERRUPCION_VACACIONES 
INNER JOIN PERSONAL 
      ON INTERRUPCION_VACACIONES.COMPANIA = PERSONAL.COMPANIA
     AND INTERRUPCION_VACACIONES.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
LEFT JOIN LIBRO_INTERRUPCION_HIS_091 
     ON INTERRUPCION_VACACIONES.COMPANIA              = LIBRO_INTERRUPCION_HIS_091.COMPANIA 
    AND INTERRUPCION_VACACIONES.ID_DE_PROCESO         = LIBRO_INTERRUPCION_HIS_091.ID_DE_PROCESO 
    AND INTERRUPCION_VACACIONES.ID_DE_EMPLEADO        = LIBRO_INTERRUPCION_HIS_091.ID_DE_EMPLEADO
    AND INTERRUPCION_VACACIONES.FECHAPAGOINTERRUPCION = LIBRO_INTERRUPCION_HIS_091.FECHA
LEFT JOIN LIBRO_INTERRUPCION_VAC_FIN 
      ON INTERRUPCION_VACACIONES.COMPANIA       = LIBRO_INTERRUPCION_VAC_FIN.COMPANIA 
     AND INTERRUPCION_VACACIONES.ID_DE_PROCESO  = LIBRO_INTERRUPCION_VAC_FIN.ID_DE_PROCESO 
     AND INTERRUPCION_VACACIONES.ID_DE_EMPLEADO = LIBRO_INTERRUPCION_VAC_FIN.ID_DE_EMPLEADO 
     AND INTERRUPCION_VACACIONES.ID_DE_CONCEPTO = LIBRO_INTERRUPCION_VAC_FIN.ID_DE_CONCEPTO 
     AND INTERRUPCION_VACACIONES.FECHA_INICIO   = LIBRO_INTERRUPCION_VAC_FIN.INI_PER 
     AND INTERRUPCION_VACACIONES.FECHA_FINAL    = LIBRO_INTERRUPCION_VAC_FIN.FIN_PER        
LEFT JOIN LIBRO_INTERRUPCION_HIS_099
      ON LIBRO_INTERRUPCION_VAC_FIN.MES               = LIBRO_INTERRUPCION_HIS_099.MES 
     AND LIBRO_INTERRUPCION_VAC_FIN.ANO               = LIBRO_INTERRUPCION_HIS_099.ANO 
     AND LIBRO_INTERRUPCION_VAC_FIN.COMPANIA          = LIBRO_INTERRUPCION_HIS_099.COMPANIA 
     AND LIBRO_INTERRUPCION_VAC_FIN.ID_DE_PROCESO     = LIBRO_INTERRUPCION_HIS_099.ID_DE_PROCESO 
     AND LIBRO_INTERRUPCION_VAC_FIN.FECHA_RENOVACION  = LIBRO_INTERRUPCION_HIS_099.FECHA
     AND LIBRO_INTERRUPCION_VAC_FIN.ID_DE_EMPLEADO    = LIBRO_INTERRUPCION_HIS_099.ID_DE_EMPLEADO
LEFT JOIN LIBRO_INTERRUPCION_VAC_INI
      ON INTERRUPCION_VACACIONES.COMPANIA         = LIBRO_INTERRUPCION_VAC_INI.COMPANIA
     AND INTERRUPCION_VACACIONES.ID_DE_PROCESO    = LIBRO_INTERRUPCION_VAC_INI.ID_DE_PROCESO
     AND INTERRUPCION_VACACIONES.ID_DE_EMPLEADO   = LIBRO_INTERRUPCION_VAC_INI.ID_DE_EMPLEADO 
     AND INTERRUPCION_VACACIONES.ID_DE_CONCEPTO   = LIBRO_INTERRUPCION_VAC_INI.ID_DE_CONCEPTO
     AND INTERRUPCION_VACACIONES.FECHA_INICIO     = LIBRO_INTERRUPCION_VAC_INI.FECHA_INICIO 
     AND INTERRUPCION_VACACIONES.FECHA_FINAL      = LIBRO_INTERRUPCION_VAC_INI.FECHA_FINAL 
     AND INTERRUPCION_VACACIONES.INICIO_DISFRUTE  = LIBRO_INTERRUPCION_VAC_INI.INICIO_DISFRUTE
     AND INTERRUPCION_VACACIONES.FINAL_DISFRUTE   = LIBRO_INTERRUPCION_VAC_INI.FINAL_DISFRUTE 
WHERE INTERRUPCION_VACACIONES.COMPANIA      = s$compania$s
  AND INTERRUPCION_VACACIONES.ID_DE_PROCESO = s$procesoNomina$s
  AND INTERRUPCION_ENDINERO                 = 0
