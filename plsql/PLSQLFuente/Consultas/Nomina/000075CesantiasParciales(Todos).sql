SELECT CESANTIAS.ID_DE_EMPLEADO,
       CESANTIAS.FECHA,
       CESANTIAS.CESANTIA,
       CESANTIAS.INTERES,
       PERSONAL.NOMBRECOMPLETO,
       CESANTIAS.COMPANIA,
       CASE WHEN CESANTIAS.CONSIGNADAS NOT IN(0) THEN 'X' ELSE ' ' END  CONSIGNADAS
FROM CESANTIAS
INNER JOIN PERSONAL 
 ON  CESANTIAS.COMPANIA          = PERSONAL.COMPANIA 
 AND CESANTIAS.ID_DE_EMPLEADO    = PERSONAL.ID_DE_EMPLEADO 
WHERE CESANTIAS.COMPANIA         = s$compania$s
ORDER BY CESANTIAS.ID_DE_EMPLEADO, 
         CESANTIAS.FECHA
