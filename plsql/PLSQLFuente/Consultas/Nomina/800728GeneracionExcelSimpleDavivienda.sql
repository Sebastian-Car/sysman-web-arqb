MERGE INTO CONSULTAS FIN USING (SELECT '800728GeneracionExcelSimpleDavivienda' INFORME ,TO_CLOB(q'[SELECT
            CASE PERSONAL.DCTO_IDENTIDAD
                WHEN 'C' THEN '01'
                WHEN 'E' THEN '02'
                WHEN 'N' THEN '03'                
                WHEN 'T' THEN '04'                
                WHEN 'P' THEN '05'
                ELSE '99'
            END AS "TIPO DE IDENTIFICACION",
            PERSONAL.NUMERO_DCTO "NUMERO DE IDENTIFICACION",
            PERSONAL.NOMBRES "NOMBRE",
            PERSONAL.APELLIDO1 ||' ' || PERSONAL.APELLIDO2 "APELLIDO",
            BANCOS_NOMINA.CODIGO_ENTIDAD "CODIGO DEL BANCO",
            CASE PERSONAL.TIPOCUENTA
                WHEN 'A' THEN 'CA'
                WHEN 'C' THEN 'CC'
                ELSE 'OP'
            END "TIPO DE PRODUCTO O SERVICIO", 
            PERSONAL.CUENTA "NUMERO DEL PRODUCTO O SERVICIO",
            TO_CHAR((SUM(HISTORICOS.VALOR))) "VALOR DEL PAGO O DE LA RECARGA"
        FROM
            HISTORICOS
            INNER JOIN PERSONAL
                ON HISTORICOS.COMPANIA = PERSONAL.COMPANIA
                AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
            INNER JOIN BANCOS_NOMINA
                ON PERSONAL.BANCO = BANCOS_NOMINA.BANCO
                AND PERSONAL.COMPANIA = BANCOS_NOMINA.COMPANIA
        WHERE
            HISTORICOS.ANO = s$ano$s
            AND HISTORICOS.MES = s$mes$s
            AND HISTORICOS.ID_DE_PROCESO = s$proceso$s
            AND HISTORICOS.PERIODO = s$periodo$s
            AND HISTORICOS.COMPANIA = s$compania$s
            AND HISTORICOS.ID_DE_CONCEPTO = '144'
            AND PERSONAL.BANCO NOT IN ('00','98','99')
            AND BANCOS_NOMINA.CUENTA IS NOT NULL
            AND BANCOS_NOMINA.CUENTA NOT IN ('NA')
            AND BANCOS_NOMINA.BANCO = CASE WHEN s$todosLosBancos$s <> 0 THEN BANCOS_NOMINA.BANCO ELSE 's$banco$s' END
          GROUP BY
            PERSONAL.NUMERO_DCTO,
            PERSONAL.NOMBRES,
            PERSONAL.APELLIDO1 ||' ' || PERSONAL.APELLIDO2,
            BANCOS_NOMINA.CODIGO_ENTIDAD,
            PERSONAL.CUENTA,
            PERSONAL.TIPOCUENTA,
            PERSONAL.DCTO_IDENTIDAD]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, 'MPEREZ' CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);