MERGE INTO CONSULTAS FIN USING (SELECT '800663ConceptosSinConfigurar' INFORME ,TO_CLOB(q'[SELECT TO_CHAR(CONCEPTOS.CLASE) AS CLASE, 
TO_CHAR(HISTORICOS.ANO) AS ANO, 
TO_CHAR(HISTORICOS.ID_DE_CONCEPTO) AS ID_DE_CONCEPTO, 
CONCEPTOS.NOMBRE_CONCEPTO,
SUM(HISTORICOS.VALOR) AS VALOR
FROM HISTORICOS 
INNER JOIN CONCEPTOS 
 ON HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA 
AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO 
LEFT JOIN CONCEPTO_CENTROCOSTO 
 ON HISTORICOS.COMPANIA = CONCEPTO_CENTROCOSTO.COMPANIA 
AND HISTORICOS.ANO = CONCEPTO_CENTROCOSTO.ANO
AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTO_CENTROCOSTO.ID_DE_CONCEPTO
WHERE HISTORICOS.COMPANIA = s$compania$s
AND HISTORICOS.ANO = s$anio$s
AND HISTORICOS.ID_DE_PROCESO = 01
AND CONCEPTOS.CLASE IN (3)
AND CONCEPTO_CENTROCOSTO.ID_DE_CONCEPTO IS NULL 
GROUP BY CONCEPTOS.CLASE, TO_CHAR(CONCEPTOS.CLASE), CONCEPTOS.CLASE, TO_CHAR(HISTORICOS.ANO), CONCEPTOS.CLASE, 
TO_NUMBER(HISTORICOS.ANO), HISTORICOS.ANO, CONCEPTOS.CLASE, HISTORICOS.ANO, HISTORICOS.ID_DE_CONCEPTO, 
TO_NUMBER(HISTORICOS.ID_DE_CONCEPTO), HISTORICOS.ID_DE_CONCEPTO, HISTORICOS.ID_DE_CONCEPTO, HISTORICOS.ANO, CONCEPTOS.NOMBRE_CONCEPTO, 
TO_CHAR(HISTORICOS.ID_DE_CONCEPTO), HISTORICOS.ID_DE_CONCEPTO, HISTORICOS.ID_DE_CONCEPTO, HISTORICOS.ID_DE_CONCEPTO
ORDER BY HISTORICOS.ID_DE_CONCEPTO]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);