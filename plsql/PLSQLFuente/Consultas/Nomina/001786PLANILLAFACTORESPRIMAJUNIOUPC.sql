SELECT HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  UPPER(PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES)) MES,
  HISTORICOS.PERIODO,
  HISTORICOS.ID_DE_EMPLEADO,
  PERSONAL.NOMBRECOMPLETO NOMCOMPLETO,
  PERSONAL.NUMERO_DCTO CEDULA,
  PERSONAL.FECHA_DE_INGRESO,
  PERSONAL.ID_CENTRO_DE_COSTO,
  PERSONAL.ID_DE_CARGO,
  CENTRO_COSTO.NOMBRE NOMBRE_CENTRO_DE_COSTO,
  SUM(CASE    WHEN HISTORICOS.ID_DE_CONCEPTO = 1     THEN VALOR     ELSE 0   END +
	  CASE    WHEN HISTORICOS.ID_DE_CONCEPTO = 62    THEN VALOR     ELSE 0   END) SUELDO,
  SUM(CASE    WHEN HISTORICOS.ID_DE_CONCEPTO = 952   THEN VALOR     ELSE 0   END) DIASLABORADOS,
  SUM(CASE    WHEN HISTORICOS.ID_DE_CONCEPTO = 81    THEN VALOR     ELSE 0   END) AUXTRANSPORTE,
  SUM(CASE    WHEN HISTORICOS.ID_DE_CONCEPTO = 79    THEN VALOR     ELSE 0   END) SUBALIMENTACION,
  SUM(CASE    WHEN HISTORICOS.ID_DE_CONCEPTO = 193   THEN VALOR     ELSE 0   END) BONIFICACION,
  SUM(CASE    WHEN HISTORICOS.ID_DE_CONCEPTO IN (193,197,198,199)     THEN VALOR     ELSE 0   END ) EDUCACIONAVANZADA,
  SUM(CASE    WHEN HISTORICOS.ID_DE_CONCEPTO = 955   THEN VALOR     ELSE 0   END) BONIFICACIONANUAL,
  CASE WHEN SUM(CASE     WHEN HISTORICOS.ID_DE_CONCEPTO = 9     THEN VALOR      ELSE 0     END) = 0
	   THEN 0
	   ELSE (360 * SUM(CASE    WHEN HISTORICOS.ID_DE_CONCEPTO = 160   THEN VALOR     ELSE 0     END)) / 
				   SUM(CASE    WHEN HISTORICOS.ID_DE_CONCEPTO = 9     THEN VALOR     ELSE 0     END)  END PROMEDIOPRIMA,
  SUM(CASE    WHEN HISTORICOS.ID_DE_CONCEPTO = 160   THEN VALOR     ELSE 0   END) VALORPRIMA,
  SUM(CASE    WHEN HISTORICOS.ID_DE_CONCEPTO = 140   THEN VALOR     ELSE 0   END) DEDUCCIONES,
  SUM(CASE    WHEN HISTORICOS.ID_DE_CONCEPTO = 144   THEN VALOR     ELSE 0   END) NETO
FROM HISTORICOS
  INNER JOIN PERSONAL
     ON HISTORICOS.COMPANIA       = PERSONAL.COMPANIA
    AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
  INNER JOIN CENTRO_COSTO
     ON PERSONAL.COMPANIA           = CENTRO_COSTO.COMPANIA
    AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
    AND PERSONAL.ANO                = CENTRO_COSTO.ANO
WHERE PERSONAL.COMPANIA         = s$compania$s
  AND HISTORICOS.ID_DE_PROCESO    = s$idProceso$s
  AND HISTORICOS.ANO              = s$ano$s
  AND HISTORICOS.MES              = s$mes$s
  AND HISTORICOS.PERIODO          = s$periodo$s
  AND PERSONAL.ID_DE_TIPO NOT    IN (2)
GROUP BY HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  UPPER(PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES)),
  HISTORICOS.PERIODO,
  HISTORICOS.ID_DE_EMPLEADO,
  PERSONAL.NOMBRECOMPLETO,
  PERSONAL.NUMERO_DCTO,
  PERSONAL.FECHA_DE_INGRESO,
  PERSONAL.ID_CENTRO_DE_COSTO,
  PERSONAL.ID_DE_CARGO,
  CENTRO_COSTO.NOMBRE
HAVING SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 160
    THEN VALOR
    ELSE 0
  END) > 0