WITH SUMACUMULADOS AS
  (SELECT COMPANIA,
    ID_DE_EMPLEADO,
    SUM(DEVENGOS) DEVENGOS,
    SUM(DESCUENTOS) DESCUENTOS,
    SUM(NETOAPAGAR) NETO
  FROM V_ACUMULADOS
  WHERE V_ACUMULADOS.COMPANIA    = s$compania$s
  AND V_ACUMULADOS.ID_DE_PROCESO = s$proceso$s
  AND V_ACUMULADOS.ANO           = s$ano$s
  AND V_ACUMULADOS.MES           = s$mes$s
  AND V_ACUMULADOS.PERIODO       = s$periodo$s
  GROUP BY ID_DE_EMPLEADO,
    COMPANIA
  )
SELECT SUMACUMULADOS.DEVENGOS,
  SUMACUMULADOS.DESCUENTOS,
  SUMACUMULADOS.NETO,
  SUB.*
FROM
  (SELECT *
  FROM
    (SELECT V_ACUMULADOS.COMPANIA,
      V_ACUMULADOS.ID_DE_EMPLEADO,
      PERSONAL.NUMERO_DCTO CEDULA,
      PERSONAL.NOMBRECOMPLETO NOMBRE_EMPLEADO,
      V_ACUMULADOS.CUENTA,
      V_ACUMULADOS.VALOR VALOR,
      CONCEPTOS.CLASE
      ||'_'
      ||LPAD(CONCEPTOS.ID_DE_CONCEPTO,3,0)
      ||'_'
      || CONCEPTOS.NOMBRE_CONCEPTO NOMBRECONCEPTO
    FROM V_ACUMULADOS
    INNER JOIN PERSONAL
    ON V_ACUMULADOS.COMPANIA        = PERSONAL.COMPANIA
    AND V_ACUMULADOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
    INNER JOIN CONCEPTOS
    ON V_ACUMULADOS.COMPANIA                = CONCEPTOS.COMPANIA
    AND V_ACUMULADOS.ID_DE_CONCEPTO         = CONCEPTOS.ID_DE_CONCEPTO
    WHERE V_ACUMULADOS.COMPANIA             = s$compania$s
    AND V_ACUMULADOS.ID_DE_PROCESO          = s$proceso$s
    AND V_ACUMULADOS.ANO                    = s$ano$s
    AND V_ACUMULADOS.MES                    = s$mes$s
    AND V_ACUMULADOS.PERIODO                = s$periodo$s
    AND (V_ACUMULADOS.CLASE BETWEEN 1 AND 8
    OR V_ACUMULADOS.ID_DE_CONCEPTO < 900 )
     ) PIVOT (SUM(VALOR) FOR NOMBRECONCEPTO IN (s$condicionPivot$s))
  )SUB
INNER JOIN SUMACUMULADOS
ON SUB.COMPANIA        = SUMACUMULADOS.COMPANIA
AND SUB.ID_DE_EMPLEADO = SUMACUMULADOS.ID_DE_EMPLEADO
    ORDER BY NOMBRE_EMPLEADO	
