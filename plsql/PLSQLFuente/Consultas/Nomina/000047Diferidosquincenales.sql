SELECT PERSONAL.ID_DE_EMPLEADO,  
       PERSONAL.NUMERO_DCTO,  
       PERSONAL.NOMBRECOMPLETO,  
       CONCEPTOS.NOMBRE_CONCEPTO,  
       FINANCIABLES_DE_NOMINA.VALOR_CUOTA,  
       FINANCIABLES_DE_NOMINA.SALDO,  
       CONCEPTOS.ID_DE_CONCEPTO,  
       FINANCIABLES_DE_NOMINA.CUOTAFIJA,  
       FINANCIABLES_DE_NOMINA.ID_DE_PROCESO,  
       FINANCIABLES_DE_NOMINA.ANO,  
       FINANCIABLES_DE_NOMINA.MES,  
       FINANCIABLES_DE_NOMINA.PERIODO,  
       FINANCIABLES_DE_NOMINA.VALDESCUENTO,  
       FINANCIABLES_DE_NOMINA.NUMERO_DE_CUOTAS,  
       SALDO - VALDESCUENTO  SALDOPRESTAMO,  
       PERIODOS.FECHAFINAL,  
       FINANCIABLES_DE_NOMINA.PERIODOCOBRO,  
       FINANCIABLES_DE_NOMINA.NLIBRANZA,  
       FINANCIABLES_DE_NOMINA.FECHA_LIBRANZA,  
       FINANCIABLES_DE_NOMINA.FECHAFIN,  
       DEPENDENCIA.NOMBRE  NOMBREDEPENDENCIA,  
       ESTADO_ACTUAL.NOMBRE, 
       CASE WHEN  
        ESTADO_ACTUAL = 1 
    THEN  
        'ACTIVO' 
    ELSE 
        CASE WHEN  
           ESTADO_ACTUAL = 2 
       THEN  
           'PENSIONADO' 
       ELSE 
           CASE WHEN  
              ESTADO_ACTUAL = 3 
          THEN  
              'RETIRADO' 
          ELSE 
              CASE WHEN  
                 ESTADO_ACTUAL = 5 
             THEN  
                 'ACTIVO PARA AJUSTES' 
             ELSE 
                 'OTRO' 
             END 
          END 
       END 
    END  ESTADOACTUAL,  
       PERIODOS.COMPANIA  
FROM FINANCIABLES_DE_NOMINA  
INNER JOIN PERSONAL  
  ON FINANCIABLES_DE_NOMINA.COMPANIA        = PERSONAL.COMPANIA 
 AND FINANCIABLES_DE_NOMINA.ID_DE_EMPLEADO  = PERSONAL.ID_DE_EMPLEADO 
INNER JOIN ESTADO_ACTUAL 
  ON PERSONAL.ESTADO_ACTUAL  = ESTADO_ACTUAL.CODIGO 
INNER JOIN CONCEPTOS  
  ON FINANCIABLES_DE_NOMINA.COMPANIA        = CONCEPTOS.COMPANIA 
 AND FINANCIABLES_DE_NOMINA.ID_DE_CONCEPTO  = CONCEPTOS.ID_DE_CONCEPTO  
INNER JOIN PERIODOS  
  ON FINANCIABLES_DE_NOMINA.COMPANIA      = PERIODOS.COMPANIA 
 AND FINANCIABLES_DE_NOMINA.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO 
 AND FINANCIABLES_DE_NOMINA.ANO           = PERIODOS.ANO 
 AND FINANCIABLES_DE_NOMINA.MES           = PERIODOS.MES 
 AND FINANCIABLES_DE_NOMINA.PERIODO       = PERIODOS.PERIODO  
LEFT JOIN DEPENDENCIA  
    ON PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO 
   AND PERSONAL.COMPANIA = DEPENDENCIA.COMPANIA  
WHERE FINANCIABLES_DE_NOMINA.COMPANIA       = s$compania$s 
  AND FINANCIABLES_DE_NOMINA.ID_DE_PROCESO  = s$procesoNomina$s 
  AND FINANCIABLES_DE_NOMINA.ANO            = s$anioNomina$s 
  AND FINANCIABLES_DE_NOMINA.MES            = s$mesNomina$s 
AND FINANCIABLES_DE_NOMINA.PERIODO = s$periodoNomina$s 
ORDER BY  CONCEPTOS.ID_DE_CONCEPTO,
s$periodoNomina$s
