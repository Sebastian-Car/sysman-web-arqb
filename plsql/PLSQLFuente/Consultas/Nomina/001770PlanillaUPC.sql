SELECT 
  PERSONAL.NUMERO_DCTO CEDULA ,
  PERSONAL.NOMBRECOMPLETO NOM_EMPLEADO,
  PERSONAL.ID_CENTRO_DE_COSTO ,
  CENTRO_COSTO.NOMBRE,
  PERSONAL.DEPENDENCIA,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('210') THEN HISTORICOS.VALOR ELSE 0 END) C210,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('001') THEN HISTORICOS.VALOR ELSE 0 END) C001,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('009') THEN HISTORICOS.VALOR ELSE 0 END) C009,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('002') THEN HISTORICOS.VALOR ELSE 0 END) C002,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('062') THEN HISTORICOS.VALOR ELSE 0 END)+
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('061') THEN HISTORICOS.VALOR ELSE 0 END) GASREP,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('379') THEN HISTORICOS.VALOR ELSE 0 END) C379,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('070') THEN HISTORICOS.VALOR ELSE 0 END) C388,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('196') THEN HISTORICOS.VALOR ELSE 0 END) C196,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('079') THEN HISTORICOS.VALOR ELSE 0 END) C079,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('080') THEN HISTORICOS.VALOR ELSE 0 END) C080,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('170') THEN HISTORICOS.VALOR ELSE 0 END) C170,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('150') THEN HISTORICOS.VALOR ELSE 0 END) C150,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('062') THEN HISTORICOS.VALOR ELSE 0 END) C062,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('193') THEN HISTORICOS.VALOR ELSE 0 END) C527,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('197') THEN HISTORICOS.VALOR ELSE 0 END)+
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('198') THEN HISTORICOS.VALOR ELSE 0 END)+
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('199') THEN HISTORICOS.VALOR ELSE 0 END)EAVAN,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('166') THEN HISTORICOS.VALOR ELSE 0 END) C166,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('186') THEN HISTORICOS.VALOR ELSE 0 END) C186,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('500') THEN HISTORICOS.VALOR ELSE 0 END) C500,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('529') THEN HISTORICOS.VALOR ELSE 0 END) C529,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('097') THEN HISTORICOS.VALOR ELSE 0 END) C097,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('140') THEN HISTORICOS.VALOR ELSE 0 END) C140,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('144') THEN HISTORICOS.VALOR ELSE 0 END) C144
FROM HISTORICOS
  INNER JOIN CONCEPTOS 
    ON HISTORICOS.COMPANIA        = CONCEPTOS.COMPANIA  
    AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO 
  INNER JOIN  PERIODOS 
    ON HISTORICOS.COMPANIA        = PERIODOS.COMPANIA
    AND HISTORICOS.ID_DE_PROCESO  = PERIODOS.ID_DE_PROCESO
    AND HISTORICOS.ANO            = PERIODOS.ANO
    AND HISTORICOS.MES            = PERIODOS.MES
    AND HISTORICOS.PERIODO        = PERIODOS.PERIODO
  INNER JOIN PERSONAL 
    ON HISTORICOS.COMPANIA            = PERSONAL.COMPANIA  
    AND HISTORICOS.ID_DE_EMPLEADO     = PERSONAL.ID_DE_EMPLEADO   
  INNER JOIN CENTRO_COSTO
    ON PERSONAL.COMPANIA            = CENTRO_COSTO.COMPANIA
    AND PERSONAL.ANO                = CENTRO_COSTO.ANO
    AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
WHERE   HISTORICOS.COMPANIA = s$compania$s 
  AND HISTORICOS.ID_DE_PROCESO = s$proceso$s
  AND HISTORICOS.ANO = s$ano$s
  AND HISTORICOS.MES = s$mes$s
  AND HISTORICOS.PERIODO = s$periodo$s
  AND PERIODOS.ACUMULADO NOT IN(0)
  AND HISTORICOS.ID_DE_CONCEPTO IN('001','002','009','080','539','533','372','525','370','079','374','534','378','524','371',
                                   '061','375','062','197','516','198','186','513','535','515','531','801','529','199','560',
                                   '527','373','170','097','517','140','144','196','166','070','210','193','150')
 GROUP BY 
  PERSONAL.NUMERO_DCTO  ,
  PERSONAL.NOMBRECOMPLETO ,
  PERSONAL.ID_CENTRO_DE_COSTO ,
  CENTRO_COSTO.NOMBRE,
  PERSONAL.DEPENDENCIA
