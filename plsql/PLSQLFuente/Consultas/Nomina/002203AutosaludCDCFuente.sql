SELECT DISTINCT
personal.ID_DE_EMPLEADO,
PERSONAL.NOMBRECOMPLETO,
PERSONAL.NUMERO_DCTO AS CEDULA,
PERSONAL.FECHA_DE_INGRESO, 
HISTORICOS.ANO,
HISTORICOS.MES, 
HISTORICOS.PERIODO, 
PCK_SYSMAN_UTL.FC_NOMBRE_MES(Historicos.MES)  NOMBRE_MES,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '112' THEN HISTORICOS.VALOR ELSE 0 END) AS IBC,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '113' THEN HISTORICOS.VALOR ELSE 0 END) AS SEMP, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '116' THEN HISTORICOS.VALOR ELSE 0 END) AS SPAT,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('113','116') THEN HISTORICOS.VALOR ELSE 0 END) SEMPSPAT,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '114' THEN HISTORICOS.VALOR ELSE 0 END) AS GEMP,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '119' THEN HISTORICOS.VALOR ELSE 0 END) AS GPAT,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('114','119') THEN HISTORICOS.VALOR ELSE 0 END) GEMPGPAT,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('113','116','114','119') THEN HISTORICOS.VALOR ELSE 0 END) SEMPSPATGEMPGPAT,
V_FONDO_DE_SALUD.FONDO_SALUD,
ESTABLECIMIENTOS_DOCENTES.CODIGO AS ID_FUENTE, 
ESTABLECIMIENTOS_DOCENTES.NOMBRE AS FUENTE,
V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD
FROM 
HISTORICOS
LEFT JOIN PERSONAL
ON PERSONAL.COMPANIA = HISTORICOS.COMPANIA
AND PERSONAL.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
LEFT JOIN V_FONDO_DE_SALUD 
ON PERSONAL.COMPANIA = V_FONDO_DE_SALUD.COMPANIA 
AND PERSONAL.FONDO_SALUD = V_FONDO_DE_SALUD.FONDO_SALUD

LEFT JOIN ESTABLECIMIENTOS_DOCENTES 
ON PERSONAL.COMPANIA = ESTABLECIMIENTOS_DOCENTES.COMPANIA 
AND PERSONAL.CODIGO_ESTABLECIMIENTO = ESTABLECIMIENTOS_DOCENTES.CODIGO

LEFT JOIN PERIODOS 
ON HISTORICOS.COMPANIA = PERIODOS.COMPANIA
AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO 
AND HISTORICOS.ANO = PERIODOS.ANO 
AND HISTORICOS.MES = PERIODOS.MES 
AND HISTORICOS.PERIODO = PERIODOS.PERIODO
WHERE 
HISTORICOS.ID_DE_CONCEPTO IN ('113', '116', '114', '119', '112')
AND HISTORICOS.ID_DE_PROCESO = s$procesoNomina$s
AND HISTORICOS.COMPANIA = s$compania$s
AND PERIODOS.ACUMULADO NOT IN (0)
AND PERSONAL.ID_DE_EMPLEADO <>'00000'
AND HISTORICOS.ANO = s$anioNomina$s
AND HISTORICOS.MES = s$mesNomina$s
AND HISTORICOS.PERIODO = s$periodoNomina$s
GROUP BY 
PERSONAL.NOMBRECOMPLETO,
PERSONAL.NUMERO_DCTO,
PERSONAL.FECHA_DE_INGRESO,
HISTORICOS.ANO, 
HISTORICOS.MES, 
HISTORICOS.PERIODO,
V_FONDO_DE_SALUD.FONDO_SALUD, 
ESTABLECIMIENTOS_DOCENTES.CODIGO, 
ESTABLECIMIENTOS_DOCENTES.NOMBRE, personal.ID_DE_EMPLEADO,
V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD