SELECT 
	(PCK_NOMINA_COM2.FC_AUSENTISMO(
		UN_COMPANIA => s$compania$s,
		UN_ID_EMPLEADO => PERSONAL.ID_DE_EMPLEADO)) AUSENTISMO,
	CESANTIAS.ID_DE_EMPLEADO ID_DE_EMPLEADO, 
	(CESANTIAS.DIAS - (PCK_NOMINA_COM2.FC_AUSENTISMO(
		UN_COMPANIA => s$compania$s,
		UN_ID_EMPLEADO => PERSONAL.ID_DE_EMPLEADO))) DIASLIQUIDADOS, 
	(CESANTIAS.SUELDO + CESANTIAS.PROMVARIABLES) SALARIOLIQUIDACION, 
	((CESANTIAS.PARCIALES - CESANTIAS.RETIRADAS) - CESANTIAS.PIGNORADO) DISPONIBLE, 
	CARGOS.NOMBRE_DEL_CARGO, 
	(TRIM(PERSONAL.APELLIDO1) || ' ' || TRIM(PERSONAL.APELLIDO2) || ' ' || TRIM(PERSONAL.NOMBRES)) NOMBRECOMPLETO, 
	PERSONAL.FECHA_DE_INGRESO, 
	CESANTIAS.FECHALIQUIDACION FECHALIQUIDACION, 
	CESANTIAS.FECHA FECHA, 
	CESANTIAS.CESANTIA, 
	CESANTIAS.INTERES, 
	CESANTIAS.CONSOLIDADAS, 
	CESANTIAS.PROMVARIABLES, 
	CESANTIAS.SUELDO, 
	CESANTIAS.RETIRADAS, 
	CESANTIAS.DIAS, 
	CESANTIAS.PARCIALES, 
	CESANTIAS.PIGNORADO, 
	CESANTIAS.DIAS_LICENCIAS, 
	PERSONAL.NUMERO_DCTO
FROM CESANTIAS 
INNER JOIN PERSONAL 
ON CESANTIAS.COMPANIA = PERSONAL.COMPANIA 
AND CESANTIAS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO 
INNER JOIN CARGOS 
ON PERSONAL.COMPANIA = CARGOS.COMPANIA 
AND PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO
WHERE CESANTIAS.COMPANIA = s$compania$s  
AND CESANTIAS.FECHA = TO_DATE(s$fecha2$s,'DD/MM/YYYY') 
ORDER BY CESANTIAS.ID_DE_EMPLEADO
