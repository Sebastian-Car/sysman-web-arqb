SELECT PERSONAL.COMPANIA,
    ESTABLECIMIENTOS_DOCENTES.CODIGO AS ESTABLECIMIENTO,
    ESTABLECIMIENTOS_DOCENTES.NOMBRE AS NOMBRE_ESTABLECIMIENTO,
    PERSONAL.NOMBRECOMPLETO          AS NomCompleto,
    MAX(VACACIONES.ID_DE_PROCESO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) ID_DE_PROCESO,
    MAX(HISTORICOS.ANO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) ANO,
    MAX(HISTORICOS.MES) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) MES,
    MIN(HISTORICOS.PERIODO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) PERIODO,
    VACACIONES.ANO,
    VACACIONES.MES,
    0 AS AUSENTISMO,
    MAX(VACACIONES.FECHA_INICIO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) FECHA_INICIO,
    MAX(VACACIONES.FECHA_FINAL) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) FECHA_FINAL,
    MAX(VACACIONES.INICIO_DISFRUTE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) INICIO_DISFRUTE,
    MAX(VACACIONES.FINAL_DISFRUTE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) FINAL_DISFRUTE,
    MAX(VACACIONES.DIAS) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) DIAS,
    MAX(VACACIONES.FECHAPAGO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) FECHAPAGO,
    PERSONAL.NUMERO_DCTO,
    PERSONAL.FECHA_DE_INGRESO,
    PERSONAL.ESTADO_ACTUAL,
    CASE
      WHEN PERSONAL.FECHA_FINAL IS NULL
      THEN PERSONAL.FECHA_DE_INGRESO
      ELSE PERSONAL.FECHA_FINAL+1
    END AS FIVP,
    TO_CHAR ( TO_DATE('s$fecha$s', 'DD/MM/YYYY') , 'DD/MM/YYYY' ) AS FFVP,
    (CASE
      WHEN (TO_DATE(
        CASE
          WHEN PERSONAL.FECHA_FINAL IS NULL
          THEN PERSONAL.FECHA_DE_INGRESO
          ELSE PERSONAL.FECHA_FINAL+1
        END +365))< (TO_CHAR(TO_DATE( 's$fecha$s', 'DD/MM/YYYY') , 'DD/MM/YYYY' ))
      THEN (TO_DATE(
        CASE
          WHEN PERSONAL.FECHA_FINAL IS NULL
          THEN PERSONAL.FECHA_DE_INGRESO
          ELSE PERSONAL.FECHA_FINAL+1
        END +365))
      ELSE (TO_DATE('s$fecha$s', 'DD/MM/YYYY') )
    END ) AS FFVPX,
    TO_CHAR ( TO_DATE('s$fecha$s', 'DD/MM/YYYY') , 'DD/MM/YYYY' ) AS fechaperiodo,
    PERSONAL.ID_DE_EMPLEADO,
    (CASE
      WHEN PCK_NOMINA_COM1.FC_PARAMETRO(31,'001')='890704536-7'
      THEN (
        CASE
          WHEN PERSONAL.GRUPOCONTABLE='A'
          THEN 'ADMINISTRACION'
          WHEN PERSONAL.GRUPOCONTABLE='V'
          THEN 'POLITICA'
          WHEN PERSONAL.GRUPOCONTABLE='P'
          THEN 'GESTION'
          ELSE 'EJECUCION'
        END )
      ELSE (
        CASE
          WHEN PERSONAL.GRUPOCONTABLE='A'
          THEN 'ADMINISTRACION'
          WHEN PERSONAL.GRUPOCONTABLE='V'
          THEN 'OPERACION/VENTAS'
          WHEN PERSONAL.GRUPOCONTABLE='P'
          THEN 'PRODUCCION/PENSIONADO'
          ELSE 'OTRO'
        END )
    END) AS GrupoContable,
    MAX(TRUNC((
    CASE
      WHEN ((TRUNC(
        CASE
          WHEN PERSONAL.fecha_inicio IS NULL
          THEN PERSONAL.FECHA_DE_INGRESO + 0 
          ELSE PERSONAL.FECHA_FINAL + 0
        END ) - TO_DATE('s$fecha$s', 'DD/MM/YYYY') ) /365)>=1
      THEN ( TO_DATE(
        CASE
          WHEN PERSONAL.FECHA_FINAL IS NULL
          THEN PERSONAL.FECHA_DE_INGRESO + 0
          ELSE PERSONAL.FECHA_FINAL+1
        END) - TO_DATE('s$fecha$s', 'DD/MM/YYYY'))
      ELSE 0
    END )/365)) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) *15 AS periodosant,
    TRUNC((((
    CASE
      WHEN PERSONAL.FECHA_FINAL IS NULL
      THEN PERSONAL.FECHA_DE_INGRESO + 0
      ELSE PERSONAL.FECHA_FINAL+1 
    END) - TO_DATE('s$fecha$s', 'DD/MM/YYYY HH24:MI:SS') +1)/365))*15 AS periodos,
    PERSONAL.DEPENDENCIA,
    CARGOS.NOMBRE_DEL_CARGO|| ' '|| CATEGORIA.CODIGOGRADO AS NOMBRE_DEL_CARGO,
    CATEGORIA.CODIGOGRADO,
    PERSONAL.FECHAINIPROGVAC,
    PERSONAL.FECHAFINPROGVAC,
    MAX(VACACIONES.FINAL_DISFRUTE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS fd,
    SUM((CASE WHEN historicos.id_de_concepto='960' THEN historicos.valor ELSE 0 END )) AS FACTORESPV,
    SUM((CASE WHEN historicos.id_de_concepto='155' THEN historicos.valor ELSE 0 END)) AS PrimaVac,
    SUM((CASE WHEN historicos.id_de_concepto='174' THEN historicos.valor ELSE 0 END)) AS Vac,
    SUM((CASE WHEN historicos.id_de_concepto='175' THEN historicos.valor ELSE 0 END)) AS VACD,
    SUM((CASE WHEN historicos.id_de_concepto='151' THEN historicos.valor ELSE 0 END)) AS BER,
    SUM((CASE WHEN historicos.id_de_concepto='099' THEN historicos.valor ELSE 0 END)) AS pend,
    SUM((CASE WHEN historicos.id_de_concepto='155' THEN historicos.valor ELSE 0 END))+
        SUM((CASE WHEN historicos.id_de_concepto='174' THEN historicos.valor ELSE 0 END))+
        SUM((CASE WHEN historicos.id_de_concepto='175' THEN historicos.valor ELSE 0 END))+
        SUM((CASE WHEN historicos.id_de_concepto='151' THEN historicos.valor ELSE 0 END)) AS TOTAL,
    SUM((CASE WHEN historicos.id_de_concepto='140' AND HISTORICOS.PERIODO = PCK_SYSMAN_UTL.FC_PAR( '001','PERIODO NOMINA ADICIONAL',s$modulo$s,SYSDATE)
      THEN historicos.valor
      ELSE 0
    END)) AS DESCUENTOS,
    SUM((CASE WHEN historicos.id_de_concepto='155' THEN historicos.valor ELSE 0 END)) + 
        SUM((CASE WHEN historicos.id_de_concepto='174' THEN historicos.valor ELSE 0 END)) + 
        SUM((CASE WHEN historicos.id_de_concepto='175' THEN historicos.valor ELSE 0 END)) +
        SUM((CASE WHEN historicos.id_de_concepto='151' THEN historicos.valor ELSE 0 END)) - 
        SUM((CASE WHEN historicos.id_de_concepto='140' AND HISTORICOS.PERIODO=PCK_SYSMAN_UTL.FC_PAR( '001','PERIODO NOMINA ADICIONAL',s$modulo$s,SYSDATE) THEN historicos.valor ELSE 0 END)) AS NETOVAC,
    VACACIONES.ENDINERO,
    VACACIONES.DIASHABILES,
    PERSONAL.NUMERO_DCTO,
    CATEGORIA.SALARIO_BASE AS SALARIO_BASE
FROM ( (HISTORICOS INNER JOIN ( (PERSONAL LEFT JOIN CARGOS ON 
            PERSONAL.COMPANIA   = CARGOS.COMPANIA
            AND PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO)
    INNER JOIN CATEGORIA
        ON PERSONAL.COMPANIA          = CATEGORIA.COMPANIA 
        AND PERSONAL.ANO              = CATEGORIA.ANO
        AND PERSONAL.ID_DE_CATEGORIA = CATEGORIA.ID_DE_CATEGORIA
        AND PERSONAL.ESCALAFON       = CATEGORIA.ESCALAFON)
    ON HISTORICOS.COMPANIA        = PERSONAL.COMPANIA 
    AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO)
    INNER JOIN VACACIONES ON 
    HISTORICOS.COMPANIA           = VACACIONES.COMPANIA
    AND HISTORICOS.ID_DE_PROCESO  = VACACIONES.ID_DE_PROCESO
    AND HISTORICOS.ANO            = VACACIONES.ANO
    AND HISTORICOS.MES            = VACACIONES.MES
    AND HISTORICOS.ID_DE_EMPLEADO = VACACIONES.ID_DE_EMPLEADO)
LEFT JOIN ESTABLECIMIENTOS_DOCENTES ON 
    PERSONAL.COMPANIA = ESTABLECIMIENTOS_DOCENTES.COMPANIA 
    AND PERSONAL.CODIGO_ESTABLECIMIENTO = ESTABLECIMIENTOS_DOCENTES.CODIGO
WHERE PERSONAL.COMPANIA       = s$compania$s 
    AND HISTORICOS.ID_DE_CONCEPTO IN ('155' ,'151','174','175','099','140' ,'960' )
    AND VACACIONES.ANO            = s$anoFormulario$s
    AND VACACIONES.MES            = s$mesFormulario$s 
    AND PERSONAL.ESTADO_ACTUAL    <> 3
    AND PERSONAL.ESTADO_ACTUAL    <> 2
    AND PERSONAL.ID_DE_EMPLEADO   <> '00000'
GROUP BY PERSONAL.COMPANIA,
    ESTABLECIMIENTOS_DOCENTES.CODIGO,
    ESTABLECIMIENTOS_DOCENTES.NOMBRE,
    PERSONAL.NOMBRECOMPLETO,
    VACACIONES.ANO,
    VACACIONES.MES,
    0,
    PERSONAL.FECHA_DE_INGRESO,
    PERSONAL.ESTADO_ACTUAL,
    (CASE WHEN PERSONAL.FECHA_FINAL IS NULL
        THEN PERSONAL.FECHA_DE_INGRESO
        ELSE PERSONAL.FECHA_FINAL+1
    END),
    (CASE
        WHEN (TO_DATE(
          CASE
            WHEN PERSONAL.FECHA_FINAL IS NULL
            THEN PERSONAL.FECHA_DE_INGRESO +0
            ELSE PERSONAL.FECHA_FINAL+1
            END+365)) < (TO_CHAR( TO_DATE('s$fecha$s', 'DD/MM/YYYY') , 'DD/MM/YYYY' ))
        THEN (TO_DATE(
            CASE
            WHEN PERSONAL.FECHA_FINAL IS NULL
            THEN PERSONAL.FECHA_DE_INGRESO + 0
            ELSE PERSONAL.FECHA_FINAL+1
            END+365))
        ELSE TO_DATE('s$fecha$s', 'DD/MM/YYYY')
    END ),
    TO_CHAR ( TO_DATE('s$fecha$s', 'DD/MM/YYYY') , 'DD/MM/YYYY' ),
    PERSONAL.ID_DE_EMPLEADO,
    (CASE
        WHEN PCK_NOMINA_COM1.FC_PARAMETRO(31,'001')='890704536-7'
        THEN (
            CASE
            WHEN PERSONAL.GRUPOCONTABLE='A'
            THEN 'ADMINISTRACION'
            WHEN PERSONAL.GRUPOCONTABLE='V'
            THEN 'POLITICA'
            WHEN PERSONAL.GRUPOCONTABLE='P'
            THEN 'GESTION'
            ELSE 'EJECUCION'
            END)
        ELSE (
            CASE
            WHEN PERSONAL.GRUPOCONTABLE='A'
            THEN 'ADMINISTRACION'
            WHEN PERSONAL.GRUPOCONTABLE='V'
            THEN 'OPERACION/VENTAS'
            WHEN PERSONAL.GRUPOCONTABLE='P'
            THEN 'PRODUCCION/PENSIONADO'
            ELSE 'OTRO'
            END )
    END) ,
    TRUNC((((
    CASE
      WHEN PERSONAL.FECHA_FINAL IS NULL
      THEN PERSONAL.FECHA_DE_INGRESO + 0
      ELSE PERSONAL.FECHA_FINAL+1
    END) - TO_DATE('s$fecha$s', 'DD/MM/YYYY HH24:MI:SS') +1)/365))*15,
    PERSONAL.DEPENDENCIA,
    CARGOS.NOMBRE_DEL_CARGO|| ' '|| CATEGORIA.CODIGOGRADO,
    CATEGORIA.CODIGOGRADO,
    PERSONAL.FECHAINIPROGVAC,
    PERSONAL.FECHAFINPROGVAC,
    VACACIONES.ENDINERO,
    VACACIONES.DIASHABILES,
    PERSONAL.NUMERO_DCTO,
    CATEGORIA.SALARIO_BASE,
    TO_CHAR (TO_DATE('s$fecha$s', 'DD/MM/YYYY') , 'DD/MM/YYYY' )
ORDER BY PERSONAL.NOMBRECOMPLETO  
