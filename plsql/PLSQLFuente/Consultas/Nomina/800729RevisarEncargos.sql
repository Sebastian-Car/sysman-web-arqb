MERGE INTO CONSULTAS FIN USING (SELECT '800729RevisarEncargos' INFORME ,TO_CLOB(q'[SELECT ENCARGOS.ID_DE_EMPLEADO ID,
PERSONAL.NUMERO_DCTO CEDULA,
PERSONAL.NOMBRECOMPLETO NOMBRE, 
CARGOS.NOMBRE_DEL_CARGO "CARGO DE ENCARGO",
TO_CHAR(ENCARGOS.FECHAINICIO,'DD/MM/YYYY') "FECHA INICIO",
TO_CHAR(ENCARGOS.FECHAFINAL,'DD/MM/YYYY') "FECHA FIN",
TO_CHAR(GREATEST(TRUNC(ENCARGOS.FECHAFINAL) - TRUNC(SYSDATE),0)) "DIAS RESTANTES"
              FROM ENCARGOS
                LEFT JOIN PERSONAL
                  ON  ENCARGOS.COMPANIA       = PERSONAL.COMPANIA
                  AND ENCARGOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
                LEFT JOIN CARGOS
                  ON  ENCARGOS.COMPANIA        = CARGOS.COMPANIA
                  AND ENCARGOS.ESCALAFON       = CARGOS.ESCALAFON
                  AND ENCARGOS.ID_DE_CARGO     = CARGOS.ID_DE_CARGO
             WHERE ENCARGOS.COMPANIA        = s$compania$s
               AND ENCARGOS.FECHAFINAL BETWEEN TO_DATE('s$fechaIni$s','DD/MM/YYYY') AND TO_DATE('s$fechaFin$s','DD/MM/YYYY')
            ORDER BY  ENCARGOS.ID_DE_EMPLEADO, ENCARGOS.FECHAINICIO]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
COMMIT;