SELECT  
	PERSONAL_HISTORICO.NOMBRECOMPLETO,  
    PERSONAL_HISTORICO.NUMERO_DCTO,  
    PERSONAL_HISTORICO.ID_DE_EMPLEADO,  
    PERSONAL_HISTORICO.DEPENDENCIA,  
    PERSONAL_HISTORICO.NOMBRE_DEPENDENCIA,  
    PERSONAL_HISTORICO.NOMBRE_DE_CARGO,  
    TO_CHAR(HISTORICOS.FECHA, 'DD/MM/YYYY') FECHA,  
    CASE WHEN  
		FECHA_DE_INGRESO > TO_DATE('01/01/' || 's$ano$s', 'DD/MM/YYYY') 
    THEN  
		FECHA_DE_INGRESO  
    ELSE 
		TO_DATE('01/01/' || 's$ano$s', 'DD/MM/YYYY')  
    END  FECHA_DE_INGRESOMALLAMAS,  
    PERSONAL_HISTORICO.FECHA_DE_INGRESO,  
    SUM(CASE WHEN  
        HISTORICOS.ID_DE_CONCEPTO = 900 
    THEN  
        HISTORICOS.VALOR 
    ELSE 
        0 
    END)  SUELDOPRIMADIC,  
	 SUM(CASE WHEN  
			 HISTORICOS.ID_DE_CONCEPTO = 903 
		 THEN  
			 HISTORICOS.VALOR 
		 ELSE 
			 0 
		 END)  TRANSPRIMADIC,  
	 SUM(CASE WHEN  
			 HISTORICOS.ID_DE_CONCEPTO = 942 
		 THEN  
			 HISTORICOS.VALOR 
		 ELSE 
			 0 
		 END)  EXTRAS,  
	 SUM(CASE WHEN  
			 HISTORICOS.ID_DE_CONCEPTO = 910 
		 THEN  
			 HISTORICOS.VALOR 
		 ELSE 
			 0 
		 END)  DIASCOMPRIMADIC,  
	 SUM(CASE WHEN  
			 HISTORICOS.ID_DE_CONCEPTO = 938 
		 THEN  
			 HISTORICOS.VALOR 
		 ELSE 
			 0 
		 END)  DIASLICPRIMADIC,  
	 SUM(CASE WHEN  
			 HISTORICOS.ID_DE_CONCEPTO = 913 
		 THEN  
			 HISTORICOS.VALOR 
		 ELSE 
			 0 
		 END)  SALARIOBASE,  
	 SUM(CASE WHEN  
			 HISTORICOS.ID_DE_CONCEPTO = 177 
		 THEN  
			 HISTORICOS.VALOR 
		 ELSE 
			 0 
		 END)  CESANTIAS,  
	 SUM(CASE WHEN  
			 HISTORICOS.ID_DE_CONCEPTO = 169 
		 THEN  
			 HISTORICOS.VALOR 
		 ELSE 
			 0 
		 END)  INTERESESCESANTIAS,  
	 SUM(CASE WHEN  
			 HISTORICOS.ID_DE_CONCEPTO = 175 
		 THEN  
			 HISTORICOS.VALOR 
		 ELSE 
			 0 
		 END)  VACACIONES,  
	 SUM(CASE WHEN  
			 HISTORICOS.ID_DE_CONCEPTO = 140 
		 THEN  
			 HISTORICOS.VALOR 
		 ELSE 
			 0 
		 END)  DESCUENTOS,  
	 SUM(CASE WHEN  
			 HISTORICOS.ID_DE_CONCEPTO = 144 
		 THEN  
			 HISTORICOS.VALOR 
		 ELSE 
			 0 
		 END)  NETO  
FROM  
 PERSONAL_HISTORICO  
     INNER JOIN HISTORICOS  
     ON  
         (PERSONAL_HISTORICO.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO)  
             AND  
         (PERSONAL_HISTORICO.PERIODO = HISTORICOS.PERIODO)  
             AND  
         (PERSONAL_HISTORICO.MES = HISTORICOS.MES)  
             AND  
         (PERSONAL_HISTORICO.ANO = HISTORICOS.ANO)  
             AND  
         (PERSONAL_HISTORICO.ID_DE_PROCESO = HISTORICOS.ID_DE_PROCESO)  
             AND  
         (PERSONAL_HISTORICO.COMPANIA = HISTORICOS.COMPANIA)  
WHERE  
 ( 
     ((PERSONAL_HISTORICO.COMPANIA) = s$compania$s)  
         AND  
     ((PERSONAL_HISTORICO.ID_DE_PROCESO) = s$idProceso$s)  
         AND  
     ((PERSONAL_HISTORICO.ANO) = s$ano$s )  
         AND  
     ((PERSONAL_HISTORICO.MES) = s$mes$s)  
         AND  
     ((PERSONAL_HISTORICO.PERIODO) = s$periodo$s) 
 )  
GROUP BY  
 PERSONAL_HISTORICO.NOMBRECOMPLETO,  
 PERSONAL_HISTORICO.NUMERO_DCTO,  
 PERSONAL_HISTORICO.ID_DE_EMPLEADO,  
 PERSONAL_HISTORICO.DEPENDENCIA,  
 PERSONAL_HISTORICO.NOMBRE_DEPENDENCIA,  
 PERSONAL_HISTORICO.NOMBRE_DE_CARGO,  
 TO_CHAR(HISTORICOS.FECHA, 'DD/MM/YYYY') ,  
 CASE WHEN  
     FECHA_DE_INGRESO > TO_DATE('01/01/' || 's$ano$s', 'DD/MM/YYYY') 
 THEN  
     FECHA_DE_INGRESO 
 ELSE 
     TO_DATE('01/01/' || 's$ano$s', 'DD/MM/YYYY') 
 END,  
 PERSONAL_HISTORICO.FECHA_DE_INGRESO  
HAVING  
 (((SUM(CASE WHEN  
            HISTORICOS.ID_DE_CONCEPTO = 144 
        THEN  
            HISTORICOS.VALOR 
        ELSE 
            0 
        END)) <> 0))  
ORDER BY  
 PERSONAL_HISTORICO.NOMBRECOMPLETO
