MERGE INTO CONSULTAS FIN USING (SELECT '002875SubDetallePorConcepto' INFORME ,TO_CLOB(q'[SELECT SUM(SVALOR) VALORT FROM (SELECT H.COMPANIA,
    C.NITCOMPANIA,
    H.ID_DE_PROCESO PROCESO,
    H.ANO,H.MES,H.PERIODO,
    H.ID_DE_EMPLEADO,
    H.ID_DE_CONCEPTO CONCEPTO,
    CN.NOMBRE_CONCEPTO,
    P.NUMERO_DCTO,
    P.NOMBRECOMPLETO,
    P.ID_DE_TIPO,
    TE.NOMBRE_TIPO,
    SUM(H.VALOR) SVALOR
FROM HISTORICOS H
INNER JOIN COMPANIA C
ON H.COMPANIA = C.CODIGO
INNER JOIN CONCEPTOS CN
ON H.COMPANIA = CN.COMPANIA
AND H.ID_DE_CONCEPTO = CN.ID_DE_CONCEPTO
INNER JOIN PERSONAL P
ON H.COMPANIA = P.COMPANIA
AND H.ID_DE_EMPLEADO = P.ID_DE_EMPLEADO
LEFT JOIN TIPOS_DE_EMPLEADO TE
ON P.COMPANIA = TE.COMPANIA
AND P.ID_DE_TIPO = TE.ID_DE_TIPO
WHERE H.COMPANIA = s$compania$s
    AND H.ID_DE_PROCESO = s$proceso$s
    AND H.ANO = s$anio$s
    AND H.MES = s$mes$s
    AND H.PERIODO = s$periodo$s
    AND H.ID_DE_CONCEPTO = s$concepto$s
    AND P.ID_DE_TIPO = CASE WHEN 's$filtro$s' = 0 THEN 's$tipoEmpleado$s' ELSE P.ID_DE_TIPO END
GROUP BY H.COMPANIA,
    C.NITCOMPANIA,
    H.ID_DE_PROCESO,
    H.ANO,H.MES,H.PERIODO,
    H.ID_DE_EMPLEADO,
    H.ID_DE_CONCEPTO,
    CN.NOMBRE_CONCEPTO,
    P.NUMERO_DCTO,
    P.NOMBRECOMPLETO,
    P.ID_DE_TIPO,
    TE.NOMBRE_TIPO
ORDER BY H.ID_DE_CONCEPTO,H.ID_DE_EMPLEADO)]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);

MERGE INTO CONSULTAS_SUB FIN USING (SELECT '002874DetallePorConcepto' INFORME_PADRE, '002875SubDetallePorConcepto' INFORME_HIJO, 'PR_STRSQL_D_CONCEPTO' PARAMETRO ,NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (FIN.INFORME_PADRE = INI.INFORME_PADRE AND INI.INFORME_HIJO = FIN.INFORME_HIJO)  WHEN MATCHED THEN  UPDATE SET FIN.PARAMETRO = INI.PARAMETRO , FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME_PADRE,INFORME_HIJO, PARAMETRO,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME_PADRE,INI.INFORME_HIJO, INI.PARAMETRO,INI.CREATED_BY,SYSDATE);