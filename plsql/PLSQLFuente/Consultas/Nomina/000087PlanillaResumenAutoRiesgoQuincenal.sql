SELECT 
  V_PERSONAL.FONDO_RIESGOS,
  V_PERSONAL.NOMBRE_FONDORIESGOS NOMBRE_FONDO_RIESGOS,
  V_PERSONAL.FACTOR_RIESGO AS PORRIESGO,
  V_PERSONAL.APELLIDO1 || ' ' || V_PERSONAL.APELLIDO2 || ' ' || V_PERSONAL.NOMBRES NOM_EMPLEADO,
  V_PERSONAL.NUMERO_DCTO CEDULA,
  MIN(V_PERSONAL.SALARIO_BASE_IBC) KEEP (DENSE_RANK FIRST ORDER BY V_PERSONAL.NUMERO_DCTO)SALARIO_BASE_IBC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('209') THEN HISTORICOS.VALOR ELSE 0 END) AS C209,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('212') THEN HISTORICOS.VALOR ELSE 0 END) AS C212,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('111') THEN HISTORICOS.VALOR ELSE 0 END) AS C111,
  'Periodo' || ' ' || HISTORICOS.PERIODO || ' ' || PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES) || ' de ' || HISTORICOS.ANO NOMBREPERIODO
FROM HISTORICOS
  INNER JOIN PERIODOS
    ON HISTORICOS.COMPANIA       = PERIODOS.COMPANIA
    AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO
    AND HISTORICOS.ANO           = PERIODOS.ANO
    AND HISTORICOS.MES           = PERIODOS.MES
    AND HISTORICOS.PERIODO       = PERIODOS.PERIODO
  INNER JOIN V_PERSONAL
    ON HISTORICOS.COMPANIA        = V_PERSONAL.COMPANIA
    AND HISTORICOS.ID_DE_EMPLEADO = V_PERSONAL.ID_DE_EMPLEADO
WHERE HISTORICOS.COMPANIA  		= s$compania$s
  AND HISTORICOS.ID_DE_PROCESO 	= s$procesoNomina$s
  AND HISTORICOS.ANO 			= s$anioNomina$s
  AND HISTORICOS.MES  			= s$mesNomina$s
  AND HISTORICOS.PERIODO  		= s$periodoNomina$s
  AND PERIODOS.ACUMULADO NOT IN(0)
  AND HISTORICOS.ID_DE_CONCEPTO IN ('109','112','209','212','111')
GROUP BY  V_PERSONAL.FONDO_RIESGOS,
          V_PERSONAL.NOMBRE_FONDORIESGOS,
          V_PERSONAL.FACTOR_RIESGO,
          V_PERSONAL.APELLIDO1 || ' ' || V_PERSONAL.APELLIDO2 || ' ' || V_PERSONAL.NOMBRES ,
          V_PERSONAL.NUMERO_DCTO ,
          HISTORICOS.PERIODO,
          HISTORICOS.MES,
          HISTORICOS.ANO
ORDER BY V_PERSONAL.FONDO_RIESGOS,
 V_PERSONAL.NUMERO_DCTO
