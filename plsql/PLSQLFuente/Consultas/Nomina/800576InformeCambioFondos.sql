 ON PH.COMPANIA = P.COMPANIA
AND PH.ID_DE_PROCESO = P.ID_DE_PROCESO
LEFT JOIN PERIODOS PE
 ON PH.COMPANIA = PE.COMPANIA
AND PH.ID_DE_PROCESO = PE.ID_DE_PROCESO
