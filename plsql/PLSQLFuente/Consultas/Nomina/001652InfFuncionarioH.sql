WITH DESTINO AS(
SELECT 
  P.PAIS
 ,D.CODIGO DEPARTAMENTO
 ,C.CODIGO CIUDAD
 ,P.NOMBRE NOMPAIS
 ,D.NOMBRE NOMDEPTO
 ,C.NOMBRE NOMCIUDAD
FROM PAISES P
  INNER JOIN DEPARTAMENTO D
    ON P.PAIS = D.PAIS
  INNER JOIN CIUDAD C
    ON D.CODIGO = C.DEPARTAMENTO
   AND D.PAIS   = C.PAIS
)
SELECT 
  (PERSONAL.APELLIDO1 ||' '|| PERSONAL.APELLIDO2 ||' '|| PERSONAL.NOMBRES) FUNCIONARIO
 ,DETALLE.NUMDIASPER                                                       NDPER
 ,DETALLE.NUMDIASSINPER                                                    NDSINPER
 ,DETALLE.VALDIASPER
 ,DETALLE.VALDIASSINPER
 ,DETALLE.TOTAL                                                            TOTVIATICOS
 ,CASE WHEN CONCEPTO.TIPO_CONCEPTO IN('T')
       THEN DETALLE.TOTAL
       ELSE 0
  END TRANSPORTE
 ,DESTINO.NOMPAIS                                                          PAIS
 ,DESTINO.NOMDEPTO                                                         DEPTO
 ,DESTINO.NOMCIUDAD                                                        CIUDADES
 ,VIATICO.FECHAINICIO
 ,VIATICO.OBJETO
 ,VIATICO.FECHAFIN
FROM VI_DETALLE_VIATICOS DETALLE
  LEFT JOIN VI_VIATICOS VIATICO
    ON DETALLE.COMPANIA = VIATICO.COMPANIA 
   AND DETALLE.NUMERO   = VIATICO.CODSOLICITUD
  LEFT JOIN PERSONAL 
    ON DETALLE.COMPANIA = PERSONAL.COMPANIA
   AND DETALLE.TERCERO  = PERSONAL.NUMERO_DCTO 
  LEFT JOIN DESTINO
    ON VIATICO.PAIS         = DESTINO.PAIS
   AND VIATICO.DEPARTAMENTO = DESTINO.DEPARTAMENTO
   AND VIATICO.CIUDAD       = DESTINO.CIUDAD
 INNER JOIN VI_CONCEPTO_VIATICOS CONCEPTO
    ON DETALLE.COMPANIA = CONCEPTO.COMPANIA
   AND DETALLE.ANO      = CONCEPTO.ANO
   AND DETALLE.CONCEPTO = CONCEPTO.CODIGO_CONCEPTO
WHERE DETALLE.COMPANIA = s$compania$s
  AND VIATICO.FECHAINICIO  BETWEEN s$fechaInicial$s    AND s$fechaFinal$s 
  AND PERSONAL.NUMERO_DCTO BETWEEN s$funcionarioIni$s AND s$funcionarioFin$s
