SELECT MAX(HISTORICOS.ANO) AS ANO,
  MAX(HISTORICOS.MES) AS MES, 
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '112' THEN HISTORICOS.VALOR ELSE 0 END) AS IBC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '118' THEN HISTORICOS.VALOR ELSE 0 END) AS PENSIONEMPLEADO, 
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '120' THEN HISTORICOS.VALOR ELSE 0 END) AS ADICIONAL,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '115' THEN HISTORICOS.VALOR ELSE 0 END) AS SOLIDARIDAD, 
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '117' THEN HISTORICOS.VALOR ELSE 0 END) AS PENSIONPATRONO, 
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '117' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '118' THEN HISTORICOS.VALOR ELSE 0 END)+
     SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '115' THEN HISTORICOS.VALOR ELSE 0 END)+
        SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '120' THEN HISTORICOS.VALOR ELSE 0 END) AS TOTALAPORTES, 
  V_FONDO_DE_PENSIONES.NIT, 
  V_FONDO_DE_PENSIONES.NOMBRE_DEL_FONDO, 
  V_FONDO_DE_PENSIONES.ID_DEL_FONDO AS CODIGO_FONDO,
  CENTRO_COSTO.CODIGO, 
  CENTRO_COSTO.NOMBRE
FROM HISTORICOS 
LEFT JOIN PERSONAL 
    ON HISTORICOS.COMPANIA = PERSONAL.COMPANIA
    AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO  
LEFT JOIN V_FONDO_DE_PENSIONES
    ON PERSONAL.COMPANIA = V_FONDO_DE_PENSIONES.COMPANIA
    AND PERSONAL.ID_DEL_FONDO = V_FONDO_DE_PENSIONES.ID_DEL_FONDO 
LEFT JOIN CENTRO_COSTO
    ON  PERSONAL.COMPANIA = CENTRO_COSTO.COMPANIA
    AND PERSONAL.ANO = CENTRO_COSTO.ANO
    AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
WHERE 
  HISTORICOS.ID_DE_CONCEPTO IN ('112','115','117','118','120')
AND PERSONAL.COMPANIA = s$compania$s
AND HISTORICOS.ANO BETWEEN s$ano1$s AND s$anio2$s
AND HISTORICOS.MES BETWEEN s$mes1$s AND s$mes2$s
AND HISTORICOS.PERIODO BETWEEN s$periodo1$s AND s$periodo2$s
  s$centroP$s
GROUP BY V_FONDO_DE_PENSIONES.NIT,
V_FONDO_DE_PENSIONES.NOMBRE_DEL_FONDO,
V_FONDO_DE_PENSIONES.ID_DEL_FONDO, 
CENTRO_COSTO.CODIGO, 
CENTRO_COSTO.NOMBRE