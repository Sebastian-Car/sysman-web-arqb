SELECT 
HISTORICOS.ID_DE_EMPLEADO AS CODIGO,
CENTRO_COSTO.CODIGO AS ID_CENTRO_DE_COSTO,
CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO,
TRIM(PERSONAL.APELLIDO1 || ' ' || PERSONAL.APELLIDO2 || ' ' || PERSONAL.NOMBRES) AS NOM_EMPLEADO, 
HISTORICOS.ID_DE_PROCESO AS PROCESO,
HISTORICOS.ANO AS ANO,
HISTORICOS.MES AS MES,
PERSONAL.ID_DE_CARGO AS CARGO,
PERSONAL.NUMERO_DCTO AS CEDULA,
CARGOS.NOMBRE_DEL_CARGO AS NOMBRECARGO,
PERSONAL.DEPENDENCIA AS DEPENDENCIA,
DEPENDENCIA.NOMBRE AS NOMBREDEPENDENCIA,
HISTORICOS.PERIODO AS PERIODO,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='009' THEN HISTORICOS.VALOR ELSE 0 END) AS DIASLAB,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='130' THEN HISTORICOS.VALOR ELSE 0 END) AS DTSALUD,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='131' THEN HISTORICOS.VALOR ELSE 0 END) AS DTPENSION,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='132' THEN HISTORICOS.VALOR ELSE 0 END) AS DTF,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='125' THEN HISTORICOS.VALOR ELSE 0 END) AS DTRETEFUENTE,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='129' THEN HISTORICOS.VALOR ELSE 0 END) AS DTSINDICATO1,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='629' THEN HISTORICOS.VALOR ELSE 0 END) AS DTSINDICATO2,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='600' THEN HISTORICOS.VALOR ELSE 0 END) AS DTPOPULAR,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='601' THEN HISTORICOS.VALOR ELSE 0 END) AS DTBOGOTA,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='602' THEN HISTORICOS.VALOR ELSE 0 END) AS ORISOLU,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='603' THEN HISTORICOS.VALOR ELSE 0 END) AS COOCREDI,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='604' THEN HISTORICOS.VALOR ELSE 0 END) AS DTCOMFE,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='605' THEN HISTORICOS.VALOR ELSE 0 END) AS DTSINTRA,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='606' THEN HISTORICOS.VALOR ELSE 0 END) AS DTASO,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='607' THEN HISTORICOS.VALOR ELSE 0 END) AS DTCHU,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='608' THEN HISTORICOS.VALOR ELSE 0 END) AS DTRECORDAR,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='609' THEN HISTORICOS.VALOR ELSE 0 END) AS DTAURORA,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='610' THEN HISTORICOS.VALOR ELSE 0 END) AS DTSIMON,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='611' THEN HISTORICOS.VALOR ELSE 0 END) AS DTCOMER,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='612' THEN HISTORICOS.VALOR ELSE 0 END) AS DTBOLIVAR,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='613' THEN HISTORICOS.VALOR ELSE 0 END) AS DTQUINTERO,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='614' THEN HISTORICOS.VALOR ELSE 0 END) AS DTCUOTA,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='615' THEN HISTORICOS.VALOR ELSE 0 END) AS MULTA,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='616' THEN HISTORICOS.VALOR ELSE 0 END) AS DTDAVIVIENDA,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='617' THEN HISTORICOS.VALOR ELSE 0 END) AS DT617,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='799' THEN HISTORICOS.VALOR ELSE 0 END) AS TOTALDESCU,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='140' THEN HISTORICOS.VALOR ELSE 0 END) AS TOTALDEDUC
 FROM 
		HISTORICOS
		INNER JOIN PERSONAL
			ON HISTORICOS.COMPANIA = PERSONAL.COMPANIA
			AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
		INNER JOIN CONCEPTOS
			ON HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA
			AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
		INNER JOIN PERIODOS
			ON HISTORICOS.COMPANIA = PERIODOS.COMPANIA
			AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO
			AND HISTORICOS.ANO = PERIODOS.ANO
			AND HISTORICOS.MES = PERIODOS.MES
			AND HISTORICOS.PERIODO = PERIODOS.PERIODO
		INNER JOIN CARGOS
			ON PERSONAL.COMPANIA = CARGOS.COMPANIA
			AND PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO
		INNER JOIN CENTRO_COSTO 
          ON PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
          AND PERSONAL.COMPANIA = CENTRO_COSTO.COMPANIA
          AND PERSONAL.ANO = CENTRO_COSTO.ANO
		INNER JOIN DEPENDENCIA
			ON PERSONAL.COMPANIA = DEPENDENCIA.COMPANIA
			AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
WHERE (HISTORICOS.ID_DE_CONCEPTO='009'                 
     OR HISTORICOS.ID_DE_CONCEPTO='130'               
     OR HISTORICOS.ID_DE_CONCEPTO='131'                
     OR HISTORICOS.ID_DE_CONCEPTO='132'               
     OR HISTORICOS.ID_DE_CONCEPTO='125'                
     OR HISTORICOS.ID_DE_CONCEPTO='129'                
     OR HISTORICOS.ID_DE_CONCEPTO='629'                 
     OR HISTORICOS.ID_DE_CONCEPTO='600'                 
     OR HISTORICOS.ID_DE_CONCEPTO='601'                
     OR HISTORICOS.ID_DE_CONCEPTO='602'               
     OR HISTORICOS.ID_DE_CONCEPTO='603'                 
     OR HISTORICOS.ID_DE_CONCEPTO='604'                
     OR HISTORICOS.ID_DE_CONCEPTO='605'                 
     OR HISTORICOS.ID_DE_CONCEPTO='606'                
     OR HISTORICOS.ID_DE_CONCEPTO='607'                
     OR HISTORICOS.ID_DE_CONCEPTO='608'               
     OR HISTORICOS.ID_DE_CONCEPTO='609'               
     OR HISTORICOS.ID_DE_CONCEPTO='610'                 
     OR HISTORICOS.ID_DE_CONCEPTO='611'                
     OR HISTORICOS.ID_DE_CONCEPTO='612'                
     OR HISTORICOS.ID_DE_CONCEPTO='613'                
     OR HISTORICOS.ID_DE_CONCEPTO='614'                
     OR HISTORICOS.ID_DE_CONCEPTO='615'                 
     OR HISTORICOS.ID_DE_CONCEPTO='616'                
     OR HISTORICOS.ID_DE_CONCEPTO='617'                 
     OR HISTORICOS.ID_DE_CONCEPTO='799'                 
     OR HISTORICOS.ID_DE_CONCEPTO='140' )
AND PERSONAL.ESTADO_ACTUAL<>0 
AND PERSONAL.ESTADO_ACTUAL<>0
AND HISTORICOS.ID_DE_PROCESO=s$proceso$s
AND HISTORICOS.ANO=s$ano$s
AND HISTORICOS.MES=s$mes$s
AND HISTORICOS.PERIODO=s$periodo$s
AND PERSONAL.COMPANIA=s$compania$s
GROUP BY HISTORICOS.ID_DE_EMPLEADO,
CENTRO_COSTO.CODIGO,
CENTRO_COSTO.NOMBRE ,
HISTORICOS.ID_DE_PROCESO ,
HISTORICOS.ANO,
HISTORICOS.MES ,
PERSONAL.ID_DE_CARGO ,
CARGOS.NOMBRE_DEL_CARGO ,
PERSONAL.DEPENDENCIA ,
DEPENDENCIA.NOMBRE ,
PERSONAL.NUMERO_DCTO,
HISTORICOS.PERIODO,
TRIM(PERSONAL.APELLIDO1 || ' ' || PERSONAL.APELLIDO2 || ' ' || PERSONAL.NOMBRES)
 
              
