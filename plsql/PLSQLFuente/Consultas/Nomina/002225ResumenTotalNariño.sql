SELECT DISTINCT
    HISTORICOS.ID_DE_CONCEPTO,
    CONCEPTOS.NOMBRE_CONCEPTO,
    CONCEPTOS.CLASE, 
    SUM(HISTORICOS.VALOR) AS SUMA_DE_VALOR, 
    CONCEPTOS.RELACIONADO,
    PCK_NOMINA_COM6.FC_CANTIDADEMPLEADOSMES(HISTORICOS.COMPANIA,HISTORICOS.ANO,HISTORICOS.MES,HISTORICOS.PERIODO) VALOR,
    MIN(CASE WHEN CONCEPTOS.CLASE= 3 THEN 'VALOR DEVENGOS' ELSE 
          CASE WHEN CONCEPTOS.CLASE=5 THEN 'VALOR DESCUENTOS' ELSE 
           CASE WHEN CONCEPTOS.CLASE=4 THEN 'VALOR TOTAL DEVENGOS' ELSE 
             CASE WHEN CONCEPTOS.CLASE=6 THEN 'VALOR TOTAL  DESCUENTOS' ELSE
              CASE WHEN CONCEPTOS.CLASE=7 THEN 'VALOR NETO A PAGAR' ELSE 
                CASE WHEN CONCEPTOS.CLASE = 99 THEN 'BASES' ELSE 'CONCEPTOS CONTABLES'
               END
             END
            END
           END
          END
       END) AS CLASECONCEPTO,
    HISTORICOS.ID_DE_PROCESO,
    HISTORICOS.ANO,
    HISTORICOS.MES,
    HISTORICOS.PERIODO
FROM HISTORICOS
LEFT JOIN CONCEPTOS
    ON HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA
    AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO 
LEFT JOIN PERIODOS 
    ON  HISTORICOS.COMPANIA = PERIODOS.COMPANIA
    AND HISTORICOS.ANO = PERIODOS.ANO
    AND HISTORICOS.MES = PERIODOS.MES
    AND HISTORICOS.PERIODO = PERIODOS.PERIODO 
    AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO
LEFT JOIN PERSONAL 
    ON HISTORICOS.COMPANIA = PERSONAL.COMPANIA
    AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
WHERE HISTORICOS.COMPANIA=s$compania$s
AND HISTORICOS.ID_DE_PROCESO = s$proceso$s 
AND HISTORICOS.ANO  BETWEEN s$ano1$s  AND s$ano2$s
AND HISTORICOS.MES  BETWEEN s$mes1$s  AND s$mes2$s
AND HISTORICOS.PERIODO  BETWEEN s$periodo1$s  AND s$periodo2$s
GROUP BY  
HISTORICOS.ID_DE_CONCEPTO, 
CONCEPTOS.NOMBRE_CONCEPTO, 
CONCEPTOS.CLASE,
CONCEPTOS.RELACIONADO, 
HISTORICOS.ID_DE_PROCESO, 
HISTORICOS.ANO, 
HISTORICOS.MES, 
HISTORICOS.PERIODO,
HISTORICOS.COMPANIA
HAVING  (MIN(CLASE) IN (3,4,5,6,7,8,99))
ORDER BY HISTORICOS.ID_DE_CONCEPTO