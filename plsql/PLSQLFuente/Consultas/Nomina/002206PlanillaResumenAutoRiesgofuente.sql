SELECT DISTINCT
PERSONAL.FONDO_SALUD,
PERSONAL.FONDO_RIESGOS,
PERSONAL.ID_DEL_FONDO, 
V_FONDO_DE_PENSIONES.NOMBRE_DEL_FONDO,
V_FONDO_DE_RIESGOS.NOMBRE_FONDO_RIESGOS, 
V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD,
PERSONAL.DEPENDENCIA  DEPENDENCIA,
DEPENDENCIA.NOMBRE  NOMBREDEPENDENCIA, 
PERSONAL.SALARIO_BASE_IBC  SALARIO_BASE_IBC, 
HISTORICOS.ID_DE_PROCESO  PROCESO,
HISTORICOS.ANO  ANO,
HISTORICOS.MES  MES, 
PERSONAL.ID_DE_CARGO  CARGO,
CARGOS.NOMBRE_DEL_CARGO  NOMBRECARGO,
HISTORICOS.PERIODO  PERIODO, 
PERSONAL.NOMBRECOMPLETO  NOM_EMPLEADO,
PERSONAL.NUMERO_DCTO  CEDULA, 
PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES)  NOMBRE_MES,
SUM(CASE WHEN CONCEPTOS.CLASE = 3 THEN HISTORICOS.VALOR ELSE 0 END) DEVENGOS,
SUM(CASE WHEN CONCEPTOS.CLASE = 5 THEN HISTORICOS.VALOR ELSE 0 END) DESCUENTOS,
CENTRO_COSTO.CODIGO  ID_CENTRO_COSTO,
CENTRO_COSTO.NOMBRE  NOMBRE_CENTRO_COSTO,
SUM(CASE WHEN CONCEPTOS.CLASE = 3 THEN HISTORICOS.VALOR ELSE 0 END) -
SUM(CASE WHEN CONCEPTOS.CLASE = 5 THEN HISTORICOS.VALOR ELSE 0 END) NETO,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '109' THEN HISTORICOS.VALOR ELSE 0 END) AS PORRIESGO,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '111' THEN HISTORICOS.VALOR ELSE 0 END) AS C111,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '209' THEN HISTORICOS.VALOR ELSE 0 END) AS C209,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '212' THEN HISTORICOS.VALOR ELSE 0 END) AS C212,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '001' THEN HISTORICOS.VALOR ELSE 0 END) AS C001,
ESTABLECIMIENTOS_DOCENTES.CODIGO AS ID_FUENTE,
ESTABLECIMIENTOS_DOCENTES.NOMBRE AS NOMBRE_FUENTE
FROM HISTORICOS 
LEFT JOIN PERIODOS 
ON HISTORICOS.PERIODO = PERIODOS.PERIODO
AND HISTORICOS.MES = PERIODOS.MES
AND HISTORICOS.ANO = PERIODOS.ANO
AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO
AND HISTORICOS.COMPANIA = PERIODOS.COMPANIA
LEFT JOIN CONCEPTOS 
ON HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
AND HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA
LEFT JOIN PERSONAL 
ON HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
AND HISTORICOS.COMPANIA = PERSONAL.COMPANIA
LEFT JOIN V_FONDO_DE_RIESGOS 
ON PERSONAL.FONDO_RIESGOS = V_FONDO_DE_RIESGOS.FONDO_RIESGOS 
AND PERSONAL.COMPANIA = V_FONDO_DE_RIESGOS.COMPANIA
LEFT JOIN V_FONDO_DE_PENSIONES 
ON PERSONAL.ID_DEL_FONDO = V_FONDO_DE_PENSIONES.ID_DEL_FONDO
AND PERSONAL.COMPANIA = V_FONDO_DE_PENSIONES.COMPANIA
LEFT JOIN V_FONDO_DE_SALUD 
ON PERSONAL.FONDO_SALUD = V_FONDO_DE_SALUD.FONDO_SALUD
AND PERSONAL.COMPANIA = V_FONDO_DE_SALUD.COMPANIA 
LEFT JOIN DEPENDENCIA 
ON PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
AND PERSONAL.COMPANIA = DEPENDENCIA.COMPANIA
LEFT JOIN CARGOS
ON PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO
AND PERSONAL.COMPANIA = CARGOS.COMPANIA
LEFT JOIN CENTRO_COSTO 
ON PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
AND PERSONAL.COMPANIA = CENTRO_COSTO.COMPANIA
AND HISTORICOS.ANO = CENTRO_COSTO.ANO
LEFT JOIN ESTABLECIMIENTOS_DOCENTES 
ON PERSONAL.COMPANIA = ESTABLECIMIENTOS_DOCENTES.COMPANIA 
AND PERSONAL.CODIGO_ESTABLECIMIENTO = ESTABLECIMIENTOS_DOCENTES.CODIGO
WHERE 
HISTORICOS.COMPANIA = s$compania$s
AND HISTORICOS.ID_DE_PROCESO= s$procesoNomina$s
AND HISTORICOS.ANO = s$anioNomina$s
AND HISTORICOS.MES = s$mesNomina$s
AND HISTORICOS.PERIODO = s$periodoNomina$s
AND PERIODOS.ACUMULADO NOT IN (0)
AND HISTORICOS.ID_DE_CONCEPTO IN ('209', '212', '111', '109','001')
GROUP BY PERSONAL.FONDO_SALUD, PERSONAL.FONDO_RIESGOS, PERSONAL.ID_DEL_FONDO, V_FONDO_DE_PENSIONES.NOMBRE_DEL_FONDO, V_FONDO_DE_RIESGOS.NOMBRE_FONDO_RIESGOS, 
V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD, PERSONAL.DEPENDENCIA, DEPENDENCIA.NOMBRE, PERSONAL.SALARIO_BASE_IBC, HISTORICOS.ID_DE_PROCESO, 
HISTORICOS.ANO, HISTORICOS.MES, PERSONAL.ID_DE_CARGO, CARGOS.NOMBRE_DEL_CARGO, HISTORICOS.PERIODO, 
PERSONAL.NOMBRECOMPLETO, PERSONAL.NUMERO_DCTO, CENTRO_COSTO.CODIGO, CENTRO_COSTO.NOMBRE, ESTABLECIMIENTOS_DOCENTES.CODIGO, 
ESTABLECIMIENTOS_DOCENTES.NOMBRE