SELECT HISTORICOS.COMPANIA,
  HISTORICOS.ID_DE_PROCESO,
  TO_CHAR(HISTORICOS.ANO) ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  HISTORICOS.ID_DE_CONCEPTO,
  SUM(HISTORICOS.VALOR) AS TOTAL,
  CATEGORIA.SALARIO_BASE,
  TO_NUMBER(PCK_SYSMAN_UTL.FC_ROUND(UN_VALOR => CATEGORIA.SALARIO_BASE /2 , UN_PRECISION => 0)) AS "50%_TOPEMAXIMO",
  TO_NUMBER(PCK_SYSMAN_UTL.FC_ROUND(UN_VALOR => CATEGORIA.SALARIO_BASE/2, UN_PRECISION => 0)-HISTORICOS.VALOR) AS MAYOR,
  PERSONAL.ID_DE_EMPLEADO,
  PERSONAL.APELLIDO1,
  PERSONAL.APELLIDO2,
  PERSONAL.NOMBRES
FROM HISTORICOS
  INNER JOIN PERSONAL
    ON HISTORICOS.COMPANIA        = PERSONAL.COMPANIA 
    AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
  LEFT JOIN CATEGORIA
     ON PERSONAL.COMPANIA         = CATEGORIA.COMPANIA 
    AND PERSONAL.ESCALAFON        = CATEGORIA.ESCALAFON 
    AND PERSONAL.ID_DE_CATEGORIA  = CATEGORIA.ID_DE_CATEGORIA
    AND PERSONAL.ANO              = CATEGORIA.ANO
WHERE HISTORICOS.COMPANIA       = s$compania$s
  AND HISTORICOS.ID_DE_PROCESO  = s$proceso$s
  AND HISTORICOS.ANO            = s$ano$s
  AND HISTORICOS.MES            = s$mes$s
  AND HISTORICOS.PERIODO        = s$periodo$s
  AND HISTORICOS.ID_DE_CONCEPTO ='070' 
  AND TO_NUMBER(PCK_SYSMAN_UTL.FC_ROUND(UN_VALOR => CATEGORIA.SALARIO_BASE/2, UN_PRECISION => 0)-HISTORICOS.VALOR) <0
GROUP BY  HISTORICOS.COMPANIA,
          HISTORICOS.ID_DE_PROCESO,
          HISTORICOS.ANO,
          HISTORICOS.MES,
          HISTORICOS.PERIODO,
          HISTORICOS.ID_DE_CONCEPTO,
          CATEGORIA.SALARIO_BASE,
          TO_NUMBER(PCK_SYSMAN_UTL.FC_ROUND(UN_VALOR => CATEGORIA.SALARIO_BASE/2, UN_PRECISION => 0)-HISTORICOS.VALOR),
          PERSONAL.ID_DE_EMPLEADO,
          PERSONAL.APELLIDO1,
          PERSONAL.APELLIDO2,
          PERSONAL.NOMBRES
ORDER BY TO_NUMBER(PCK_SYSMAN_UTL.FC_ROUND(UN_VALOR => CATEGORIA.SALARIO_BASE/2, UN_PRECISION => 0)-HISTORICOS.VALOR)
