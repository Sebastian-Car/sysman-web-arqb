SELECT  ESCAL AS ESCALAFON,
  NOMBREESCALAFON AS NIVEL, 
  PERSON_PERMAIND_M AS PERMANENTE_MUJER,
  PERSON_PERMAIND_H AS PERMANENTE_HOMBRE,
  TOTPERMANENTE AS PERMANENTE_TOTAL,
  PERSON_TEMPFIJO_M AS FIJO_MUJER,
  PERSON_TEMPFIJO_H AS FIJO_HOMBRE,
  TOTFIJO AS FIJO_TOTAL,
  APRENDIZSENA_M AS SENA_MUJER,
  APRENDIZSENA_H AS SENA_HOMBRE, 
  TOTSENAS  AS SENA_TOTAL,
  (TOTPERMANENTE + TOTFIJO + TOTSENAS) AS TOTAL 
  FROM
(
    SELECT ACUM_DANE_DISTIC.ESCALAFON AS ESCAL, 
    ESCALAFON.NOMBRE AS NOMBREESCALAFON,
      Sum(CASE WHEN sexo='M' And DE_CARRERA='CI' And ESCALAFON<>'13' THEN 1 ELSE 0 END) AS PERSON_PERMAIND_H,
      Sum(CASE WHEN sexo='F' And DE_CARRERA='CI' And ESCALAFON<>'13' THEN 1 ELSE 0 END) AS PERSON_PERMAIND_M,
      Sum(CASE WHEN sexo='F' And DE_CARRERA='CI' And ESCALAFON<>'13' THEN 1 ELSE 0 END)+Sum(CASE WHEN sexo='M' And DE_CARRERA='CI' And ESCALAFON<>'13' THEN 1 ELSE 0 END) AS TOTPERMANENTE,
      Sum(CASE WHEN sexo='M' And DE_CARRERA<>'CI' And ESCALAFON<>'13' THEN 1 ELSE 0 END) AS PERSON_TEMPFIJO_H,
      Sum(CASE WHEN sexo='F' And DE_CARRERA<>'CI' And ESCALAFON<>'13' THEN 1 ELSE 0 END) AS PERSON_TEMPFIJO_M, 
      Sum(CASE WHEN sexo='F' And DE_CARRERA<>'CI' And ESCALAFON<>'13' THEN 1 ELSE 0 END)+Sum(CASE WHEN sexo='M' And DE_CARRERA<>'CI' And ESCALAFON<>'13' THEN 1 ELSE 0 END) AS TOTFIJO,
      Sum(CASE WHEN sexo='M' And ESCALAFON='13' THEN 1 ELSE 0 END) AS APRENDIZSENA_H, 
      Sum(CASE WHEN sexo='F' And ESCALAFON='13' THEN 1 ELSE 0 END) AS APRENDIZSENA_M,
      Sum(CASE WHEN sexo='F' And ESCALAFON='13' THEN 1 ELSE 0 END)+ Sum(CASE WHEN sexo='M' And ESCALAFON='13' THEN 1 ELSE 0 END) AS TOTSENAS      
       FROM (
       SELECT 
        PERSONAL.COMPANIA,  
        PERSONAL.ID_DE_EMPLEADO,
        MIN(PERSONAL.ESCALAFON) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) AS ESCALAFON, 
        MIN(PERSONAL.DE_CARRERA) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) AS DE_CARRERA,
        PERSONAL.SEXO
        FROM PERSONAL LEFT JOIN 
        ESCALAFON ON 
        (PERSONAL.ESCALAFON = ESCALAFON.CODIGO) AND 
        (PERSONAL.COMPANIA = ESCALAFON.COMPANIA) 
        WHERE PERSONAL.COMPANIA IN (s$compania$s)
        GROUP BY 
        PERSONAL.COMPANIA, 
        PERSONAL.ID_DE_EMPLEADO, 
        PERSONAL.SEXO
       )ACUM_DANE_DISTIC LEFT JOIN
       ESCALAFON ON 
       ACUM_DANE_DISTIC.COMPANIA = ESCALAFON.COMPANIA AND
       ACUM_DANE_DISTIC.ESCALAFON = ESCALAFON.CODIGO
        WHERE ACUM_DANE_DISTIC.ID_DE_EMPLEADO<>0
        s$COND2$s   
        GROUP BY 
        ACUM_DANE_DISTIC.ESCALAFON,
        ESCALAFON.NOMBRE
        ORDER BY ACUM_DANE_DISTIC.ESCALAFON)
