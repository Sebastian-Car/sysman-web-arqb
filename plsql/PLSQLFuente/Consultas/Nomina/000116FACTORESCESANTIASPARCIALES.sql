SELECT PERSONAL_HISTORICO.ID_DE_EMPLEADO,
  PERSONAL_HISTORICO.NOMBRECOMPLETO,
  PERSONAL_HISTORICO.NUMERO_DCTO,
  PERSONAL_HISTORICO.NOMBRE_DE_CARGO,
  PERSONAL_HISTORICO.NOMBRE_CENTRO_DE_COSTO,
  PERSONAL_HISTORICO.FECHA_DE_INGRESO,
  HISTORICOS.FECHA FECHALIQUIDACION,
  CASE
    WHEN PERSONAL_HISTORICO.REGIMEN = 1
    THEN PERSONAL_HISTORICO.FECHA_DE_INGRESO
    ELSE
      CASE
        WHEN PERSONAL_HISTORICO.FECHA_DE_INGRESO > TO_DATE('01/01/'
          || HISTORICOS.ANO , 'DD/MM/YYYY')
        THEN PERSONAL_HISTORICO.FECHA_DE_INGRESO
        ELSE TO_DATE('01/01/'
          || HISTORICOS.ANO , 'DD/MM/YYYY')
      END
  END FECHAINICIOLIQUIDACION,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 910
    THEN HISTORICOS.VALOR
    ELSE 0
  END) DIASCES,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 912
    THEN HISTORICOS.VALOR
    ELSE 0
  END) DIASLICCES,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 900
    THEN HISTORICOS.VALOR
    ELSE 0
  END) SUELDOCES,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 904
    THEN HISTORICOS.VALOR
    ELSE 0
  END) ALIMENCES,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 903
    THEN HISTORICOS.VALOR
    ELSE 0
  END) TRANSCES,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 905
    THEN HISTORICOS.VALOR
    ELSE 0
  END) PRIMANAVCES,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 907
    THEN HISTORICOS.VALOR
    ELSE 0
  END) PRIMAVACCES,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 906
    THEN HISTORICOS.VALOR
    ELSE 0
  END) PRIMAJUNCES,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 909
    THEN HISTORICOS.VALOR
    ELSE 0
  END) PRIMAJUNCESPES,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 902
    THEN HISTORICOS.VALOR
    ELSE 0
  END) EXTRASCES,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 915
    THEN HISTORICOS.VALOR
    ELSE 0
  END) VPT,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 908
    THEN HISTORICOS.VALOR
    ELSE 0
  END) BASP,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 913
    THEN HISTORICOS.VALOR
    ELSE 0
  END) PROMEDIOCES,
  ROUND(SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 913
    THEN HISTORICOS.VALOR
    ELSE 0
  END) / 360 * SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 910
    THEN HISTORICOS.VALOR
    ELSE 0
  END), 0) TOTAL,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 911
    THEN HISTORICOS.VALOR
    ELSE 0
  END) ANTICIPOSCES,
  PCK_NOMINA.FC_CESANTIA(HISTORICOS.COMPANIA, HISTORICOS.ID_DE_EMPLEADO, 'P') PIGNORADAS,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 177
    THEN HISTORICOS.VALOR
    ELSE 0
  END) CES,
  SUM(
  CASE
    WHEN 's$pagoInteresParcial$s' = 'SI'
    AND HISTORICOS.ID_DE_CONCEPTO = 169
    THEN HISTORICOS.VALOR
    ELSE 0
  END) INTCESPAGAR,
  CASE
    WHEN PERSONAL_HISTORICO.REGIMEN = 1
    THEN 'RETROACTIVAS'
    ELSE 'LEY 50/90'
  END REGIMENCESANTIAS,
  PERSONAL_HISTORICO.FONDO_CESANTIAS,
  PERSONAL_HISTORICO.NOMBRE_FONDOCESANTIAS,
  PERSONAL_HISTORICO.ID_CENTRO_DE_COSTO ,
  0 MONEDA_CES,
  CASE
    WHEN PERSONAL_HISTORICO.FECHA_ANTICIPO_CESANTIAS < FECHA
    THEN TO_CHAR(PERSONAL_HISTORICO.FECHA_ANTICIPO_CESANTIAS, 'MM')
    ELSE TO_CHAR(HISTORICOS.FECHA, 'MM')
  END MESFECHA,
  TO_CHAR(
  CASE
    WHEN TO_NUMBER(
      CASE
        WHEN PERSONAL_HISTORICO.FECHA_ANTICIPO_CESANTIAS < FECHA
        THEN TO_CHAR(PERSONAL_HISTORICO.FECHA_ANTICIPO_CESANTIAS, 'DD')
        ELSE TO_CHAR(HISTORICOS.FECHA, 'DD')
      END ) > 30
    THEN 30
    ELSE
      CASE
        WHEN PERSONAL_HISTORICO.FECHA_ANTICIPO_CESANTIAS < FECHA
        THEN TO_NUMBER(TO_CHAR(PERSONAL_HISTORICO.FECHA_ANTICIPO_CESANTIAS, 'DD'))
        ELSE TO_NUMBER(TO_CHAR(HISTORICOS.FECHA, 'DD'))
      END
  END, '00') DIAFECHA
FROM PERSONAL_HISTORICO
INNER JOIN HISTORICOS
ON PERSONAL_HISTORICO.COMPANIA        = HISTORICOS.COMPANIA
AND PERSONAL_HISTORICO.ID_DE_PROCESO  = HISTORICOS.ID_DE_PROCESO
AND PERSONAL_HISTORICO.ANO            = HISTORICOS.ANO
AND PERSONAL_HISTORICO.MES            = HISTORICOS.MES
AND PERSONAL_HISTORICO.PERIODO        = HISTORICOS.PERIODO
AND PERSONAL_HISTORICO.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
WHERE PERSONAL_HISTORICO.COMPANIA     = s$compania$s
AND PERSONAL_HISTORICO.ID_DE_PROCESO  = s$idProceso$s
AND PERSONAL_HISTORICO.ANO            = s$ano$s
AND PERSONAL_HISTORICO.MES            = s$mes$s
AND PERSONAL_HISTORICO.PERIODO        = s$periodo$s
AND PERSONAL_HISTORICO.ID_DE_EMPLEADO = s$idEmpleado$s
AND HISTORICOS.ID_DE_CONCEPTO        IN(169,177,900,901,902,903,904,905,906,907,908,909,910,911,912,913,914,974,908)
GROUP BY PERSONAL_HISTORICO.ID_DE_EMPLEADO,
  PERSONAL_HISTORICO.NOMBRECOMPLETO,
  PERSONAL_HISTORICO.NUMERO_DCTO,
  PERSONAL_HISTORICO.NOMBRE_DE_CARGO,
  PERSONAL_HISTORICO.NOMBRE_CENTRO_DE_COSTO,
  PERSONAL_HISTORICO.FECHA_DE_INGRESO,
  HISTORICOS.FECHA,
  CASE
    WHEN PERSONAL_HISTORICO.REGIMEN = 1
    THEN PERSONAL_HISTORICO.FECHA_DE_INGRESO
    ELSE
      CASE
        WHEN PERSONAL_HISTORICO.FECHA_DE_INGRESO > TO_DATE('01/01/'
          || HISTORICOS.ANO , 'DD/MM/YYYY')
        THEN PERSONAL_HISTORICO.FECHA_DE_INGRESO
        ELSE TO_DATE('01/01/'
          || HISTORICOS.ANO , 'DD/MM/YYYY')
      END
  END,
  PCK_NOMINA.FC_CESANTIA(HISTORICOS.COMPANIA, HISTORICOS.ID_DE_EMPLEADO, 'P'),
  CASE
    WHEN PERSONAL_HISTORICO.REGIMEN = 1
    THEN 'RETROACTIVAS'
    ELSE 'LEY 50/90'
  END,
  PERSONAL_HISTORICO.FONDO_CESANTIAS,
  PERSONAL_HISTORICO.NOMBRE_FONDOCESANTIAS,
  PERSONAL_HISTORICO.ID_CENTRO_DE_COSTO ,
  CASE
    WHEN PERSONAL_HISTORICO.FECHA_ANTICIPO_CESANTIAS < FECHA
    THEN TO_CHAR(PERSONAL_HISTORICO.FECHA_ANTICIPO_CESANTIAS, 'MM')
    ELSE TO_CHAR(HISTORICOS.FECHA, 'MM')
  END ,
  TO_CHAR(
  CASE
    WHEN TO_NUMBER(
      CASE
        WHEN PERSONAL_HISTORICO.FECHA_ANTICIPO_CESANTIAS < FECHA
        THEN TO_CHAR(PERSONAL_HISTORICO.FECHA_ANTICIPO_CESANTIAS, 'DD')
        ELSE TO_CHAR(HISTORICOS.FECHA, 'DD')
      END ) > 30
    THEN 30
    ELSE
      CASE
        WHEN PERSONAL_HISTORICO.FECHA_ANTICIPO_CESANTIAS < FECHA
        THEN TO_NUMBER(TO_CHAR(PERSONAL_HISTORICO.FECHA_ANTICIPO_CESANTIAS, 'DD'))
        ELSE TO_NUMBER(TO_CHAR(HISTORICOS.FECHA, 'DD'))
      END
  END, '00')
HAVING SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 177
    THEN HISTORICOS.VALOR
    ELSE 0
  END) <>0
ORDER BY PERSONAL_HISTORICO.NOMBRECOMPLETO
