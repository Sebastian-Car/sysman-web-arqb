MERGE INTO CONSULTAS FIN USING (SELECT '002823ResumenTotalDepCaqueta' INFORME ,TO_CLOB(q'[SELECT MIN(CLASE) AS CLASE,
       ID_DE_CONCEPTO AS ID_DE_CONCEPTO,
       MIN(CASE WHEN CLASE=3  THEN 'VALOR DEVENGOS' ELSE
       CASE WHEN CLASE=5 THEN 'VALOR DESCUENTOS' ELSE
          CASE WHEN CLASE=4 THEN 'VALOR TOTAL DEVENGOS' ELSE
            CASE WHEN CLASE=6 THEN 'VALOR TOTAL DESCUENTOS' ELSE
              CASE WHEN CLASE=7 THEN 'VALOR NETO A PAGAR' ELSE
                CASE WHEN CLASE=99 THEN 'BASES' ELSE 'CONCEPTOS CONTABLES'
                END
              END
            END
          END
       END
       END)  CLASECONCEPTO,
MIN(NOMBRE_CONCEPTO) AS NOMBRE_CONCEPTO,
SUM(VALOR) AS SUMA_DE_VALOR, 
SUM(CASE WHEN ID_DE_CONCEPTO>='490' AND ID_DE_CONCEPTO<='499' THEN VALOR ELSE 0 END) AS PROVISIONES
FROM V_ACUMULADOS
WHERE COMPANIA= s$compania$s
AND (LPAD(ID_DE_PROCESO, 2, 0) || LPAD(V_ACUMULADOS.ANO, 4, 0) || LPAD(V_ACUMULADOS.MES,2, 0) || LPAD(V_ACUMULADOS.PERIODO, 2, 0)  BETWEEN 's$fechaInicial$s'  AND 's$fechaFinal$s' ) 
GROUP BY ID_DE_CONCEPTO, 
ID_DE_PROCESO || ANO || MES || PERIODO
HAVING  (MIN(CLASE) IN (3,4,5,6,7,8,99))]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);