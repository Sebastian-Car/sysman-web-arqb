MERGE INTO CONSULTAS FIN USING (SELECT '800610CesantiasAportesEnLinea' INFORME ,TO_CLOB(q'[SELECT NVL(TIPOS_DOCUMENTOS.SIGLA2,'CC') TIPO_IDENTIFICACION,
        PERSONAL_HISTORICO.NUMERO_DCTO NRO_IDENTIFICACION,
        PERSONAL_HISTORICO.APELLIDO1 PRIMER_APELLIDO,
        PERSONAL_HISTORICO.APELLIDO2 SEGUNDO_APELLIDO,
        SUBSTR(PERSONAL_HISTORICO.NOMBRES,1,DECODE(INSTR(PERSONAL_HISTORICO.NOMBRES,' '),0,LENGTH(PERSONAL_HISTORICO.NOMBRES),INSTR(PERSONAL_HISTORICO.NOMBRES, ' '))) PRIMER_NOMBRE,
        DECODE(INSTR(PERSONAL_HISTORICO.NOMBRES,' '),0,'', SUBSTR(PERSONAL_HISTORICO.NOMBRES,INSTR(PERSONAL_HISTORICO.NOMBRES,' ')+1,LENGTH(PERSONAL_HISTORICO.NOMBRES))) SEGUNDO_NOMBRE,
        V_FONDO_DE_CESANTIAS.NOMBRE_FONDO_CESANTIAS FONDO_CESANTIAS,
        PERSONAL_HISTORICO.SALARIO_BASE_IBC SALARIO,
        SUM(DECODE(HISTORICOS.ID_DE_CONCEPTO,277,HISTORICOS.VALOR,483,HISTORICOS.VALOR,0)) VALOR_CESANTIAS
FROM
     HISTORICOS 
     LEFT JOIN PERSONAL_HISTORICO 
        ON HISTORICOS.ID_DE_EMPLEADO = PERSONAL_HISTORICO.ID_DE_EMPLEADO
        AND HISTORICOS.COMPANIA = PERSONAL_HISTORICO.COMPANIA
        AND HISTORICOS.ANO = PERSONAL_HISTORICO.ANO
        AND HISTORICOS.MES = PERSONAL_HISTORICO.MES
        AND HISTORICOS.PERIODO = PERSONAL_HISTORICO.PERIODO
     LEFT JOIN V_FONDO_DE_CESANTIAS 
        ON PERSONAL_HISTORICO.FONDO_CESANTIAS = V_FONDO_DE_CESANTIAS.FONDO_CESANTIAS
        AND PERSONAL_HISTORICO.COMPANIA = V_FONDO_DE_CESANTIAS.COMPANIA
     LEFT JOIN TIPOS_DOCUMENTOS
        ON PERSONAL_HISTORICO.COMPANIA = TIPOS_DOCUMENTOS.COMPANIA
        AND PERSONAL_HISTORICO.DCTO_IDENTIDAD = TIPOS_DOCUMENTOS.DCTO_IDENTIDAD
WHERE
     PERSONAL_HISTORICO.COMPANIA = '001'
     AND HISTORICOS.ID_DE_CONCEPTO IN (277, 910, 911, 912, 913, 269, 483)
     AND HISTORICOS.ANO = s$ano$s
     AND HISTORICOS.MES = s$mes$s
     AND PERSONAL_HISTORICO.REGIMEN IN (1, 2)
GROUP BY    NVL(TIPOS_DOCUMENTOS.SIGLA2,'CC'),
            PERSONAL_HISTORICO.NUMERO_DCTO,
            PERSONAL_HISTORICO.APELLIDO1,
            PERSONAL_HISTORICO.APELLIDO2,
            PERSONAL_HISTORICO.NOMBRES,
            V_FONDO_DE_CESANTIAS.NOMBRE_FONDO_CESANTIAS,
            PERSONAL_HISTORICO.SALARIO_BASE_IBC
HAVING  SUM(DECODE(HISTORICOS.ID_DE_CONCEPTO,277,HISTORICOS.VALOR,483,HISTORICOS.VALOR,0)) <> 0
ORDER BY V_FONDO_DE_CESANTIAS.NOMBRE_FONDO_CESANTIAS, PERSONAL_HISTORICO.APELLIDO1, PERSONAL_HISTORICO.APELLIDO2, PERSONAL_HISTORICO.NOMBRES]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
