SELECT CONCEPTOS.NITCONCEPTO, 
                PERSONAL_HISTORICO.CODIGO_ESTABLECIMIENTO AS ESTABLECIMIENTO, 
                PERSONAL_HISTORICO.NOMBRECOMPLETO AS NOMCOMPLETO,  
                MAX (CONCEPTOS.NOMBRE_CONCEPTO)  KEEP  (DENSE_RANK LAST ORDER BY ROWNUM)  AS NOMBRE_CONCEPTO, 
                PERSONAL_HISTORICO.NOMBRE_ESTABLECIMIENTO AS NOMBRE_ESTABLECIMIENTO, 
                HISTORICOS.ID_DE_EMPLEADO, 
                SUM (HISTORICOS.VALOR)  AS APORTE,
                PERSONAL_HISTORICO.NUMERO_DCTO
                FROM   HISTORICOS LEFT JOIN 
                CONCEPTOS ON 
                 HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO  AND
                 HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA   LEFT JOIN
                PERSONAL_HISTORICO ON  
                HISTORICOS.ID_DE_EMPLEADO = PERSONAL_HISTORICO.ID_DE_EMPLEADO  AND 
                 HISTORICOS.COMPANIA = PERSONAL_HISTORICO.COMPANIA AND 
                 HISTORICOS.ANO = PERSONAL_HISTORICO.ANO AND
                 HISTORICOS.MES = PERSONAL_HISTORICO.MES AND
                 HISTORICOS.PERIODO = PERSONAL_HISTORICO.PERIODO
                WHERE    HISTORICOS.COMPANIA = s$compania$s 
                AND   HISTORICOS.ANO = s$ano1$s 
                AND   HISTORICOS.MES = s$mes1$s 
                AND   HISTORICOS.PERIODO = s$periodo1$s 
                AND   HISTORICOS.ID_DE_CONCEPTO NOT IN (130,131,132,124) 
                AND   CONCEPTOS.CLASE =5  
                GROUP BY CONCEPTOS.NITCONCEPTO,
                PERSONAL_HISTORICO.CODIGO_ESTABLECIMIENTO,
                PERSONAL_HISTORICO.NOMBRECOMPLETO,
                PERSONAL_HISTORICO.NOMBRE_ESTABLECIMIENTO, 
                HISTORICOS.ID_DE_EMPLEADO, 
                PERSONAL_HISTORICO.NUMERO_DCTO, 
                CONCEPTOS.CLASE
                ORDER BY CONCEPTOS.NITCONCEPTO, 
                PERSONAL_HISTORICO.CODIGO_ESTABLECIMIENTO
