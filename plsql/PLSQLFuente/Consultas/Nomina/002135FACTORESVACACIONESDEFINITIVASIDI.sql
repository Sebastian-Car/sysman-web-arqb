SELECT PERSONAL.ID_DE_EMPLEADO, 
    PERSONAL.NOMBRECOMPLETO, 
    CARGOS.NOMBRE_DEL_CARGO, 
    PERSONAL.NUMERO_DCTO,
    TO_CHAR(PERSONAL.FECHA_DE_INGRESO,'DD/MM/YYYY')FECHA_DE_INGRESO,
    TO_CHAR(PERSONAL.FECHA_INICIO,'DD/MM/YYYY')FECHA_INICIO,
    DEPENDENCIA.NOMBRE AS DEPENDENCIA,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '801' THEN HISTORICOS.VALOR ELSE 0 END)  AS FACTOR1,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '802' THEN HISTORICOS.VALOR ELSE 0 END)  AS FACTOR2,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '803' THEN HISTORICOS.VALOR ELSE 0 END)  AS FACTOR3,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '804' THEN HISTORICOS.VALOR ELSE 0 END)  AS FACTOR4,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '805' THEN HISTORICOS.VALOR ELSE 0 END)  AS FACTOR5,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '806' THEN HISTORICOS.VALOR ELSE 0 END)  AS FACTOR6,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '807' THEN HISTORICOS.VALOR ELSE 0 END)  AS FACTOR7,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '808' THEN HISTORICOS.VALOR ELSE 0 END)  AS FACTOR8,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '809' THEN HISTORICOS.VALOR ELSE 0 END)  AS FACTOR9,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '810' THEN HISTORICOS.VALOR ELSE 0 END)  AS FACTOR10,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '811' THEN HISTORICOS.VALOR ELSE 0 END)  AS FACTOR11,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '812' THEN HISTORICOS.VALOR ELSE 0 END)  AS FACTOR12,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '813' THEN HISTORICOS.VALOR ELSE 0 END)  AS FACTOR13,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '164' THEN HISTORICOS.VALOR ELSE 0 END)  AS NUMPER, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '94' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '96' THEN HISTORICOS.VALOR ELSE 0 END) AS DIASCAL, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '814' THEN HISTORICOS.VALOR ELSE 0 END)  AS ANTIGUEDAD, 
    MAX (TO_CHAR (HISTORICOS.FECHA,'DD/MM/YYYY')) FECHA,
    AVG(ROUND((PCK_SYSMAN_UTL.FC_DIASMESCOMERCIAL(PERSONAL.INICIO_DISFRUTE,PERSONAL.FECHA_DE_INGRESO))/360,0))-1 AS ANTIGUEDAD1,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '174'  THEN HISTORICOS.VALOR ELSE 0 END)  AS SALVAC,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '404'  THEN HISTORICOS.VALOR ELSE 0 END)  AS INDRET,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '175'  THEN HISTORICOS.VALOR ELSE 0 END)  AS VACDINERO,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '155'  THEN HISTORICOS.VALOR ELSE 0 END)  AS PRIMVAC,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '801'  THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '802' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '803' THEN HISTORICOS.VALOR ELSE 0 END)
     +SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '804' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '805' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '806' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '807' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '808' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '809' THEN HISTORICOS.VALOR ELSE 0 END)+
     SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO  = '810' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '811' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '812' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '813' THEN HISTORICOS.VALOR ELSE 0 END)     AS TOTALFACTOR,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '174'  THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '175' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '155' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '873'  THEN HISTORICOS.VALOR ELSE 0 END)    AS TOTALPAGAR, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '166'  THEN HISTORICOS.VALOR ELSE 0 END) AS DIASVAC,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '873'  THEN HISTORICOS.VALOR ELSE 0 END) AS BONIRECRE,
    TO_CHAR(PERSONAL.FECHA_DE_RETIRO,'DD/MM/YYYY')FECHA_DE_RETIRO,
    PERSONAL.ESTADO_ACTUAL, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '162'  THEN HISTORICOS.VALOR ELSE 0 END) AS DIASHAB,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '96'  THEN HISTORICOS.VALOR ELSE 0 END) AS DIASCALEND,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '92'  THEN HISTORICOS.VALOR ELSE 0 END) AS DIASBRECR,
    CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO, 
    PERSONAL.DE_CARRERA
FROM HISTORICOS
LEFT JOIN PERSONAL
    ON PERSONAL.COMPANIA = HISTORICOS.COMPANIA
    AND PERSONAL.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
LEFT JOIN PERIODOS
    ON PERIODOS.COMPANIA = HISTORICOS.COMPANIA
    AND PERIODOS.ID_DE_PROCESO = HISTORICOS.ID_DE_PROCESO
	AND PERIODOS.ANO = HISTORICOS.ANO
	AND PERIODOS.MES = HISTORICOS.MES
	AND PERIODOS.PERIODO = HISTORICOS.PERIODO
LEFT JOIN CENTRO_COSTO
    ON CENTRO_COSTO.COMPANIA = PERSONAL.COMPANIA
    AND CENTRO_COSTO.CODIGO = PERSONAL.ID_CENTRO_DE_COSTO
    AND CENTRO_COSTO.ANO = PERSONAL.ANO
LEFT JOIN DEPENDENCIA
    ON DEPENDENCIA.COMPANIA = PERSONAL.COMPANIA
    AND DEPENDENCIA.CODIGO = PERSONAL.DEPENDENCIA
LEFT JOIN CARGOS
    ON CARGOS.COMPANIA = PERSONAL.COMPANIA
    AND CARGOS.ID_DE_CARGO = PERSONAL.ID_DE_CARGO
WHERE HISTORICOS.COMPANIA = s$compania$s
     AND PERIODOS.ANO                = s$anio$s
     AND PERIODOS.MES                = s$mes$s
     AND PERIODOS.PERIODO            = s$periodo$s
     AND PERIODOS.ID_DE_PROCESO      = s$proceso$s
     AND(HISTORICOS.ID_DE_CONCEPTO > '800'
     AND HISTORICOS.ID_DE_CONCEPTO < '875'
     OR HISTORICOS.ID_DE_CONCEPTO = '094' 
     OR HISTORICOS.ID_DE_CONCEPTO = '501' 
     OR HISTORICOS.ID_DE_CONCEPTO = '516' 
     OR HISTORICOS.ID_DE_CONCEPTO = '162' 
     OR HISTORICOS.ID_DE_CONCEPTO = '096'
     OR HISTORICOS.ID_DE_CONCEPTO = '164' 
     OR HISTORICOS.ID_DE_CONCEPTO = '155'  
     OR HISTORICOS.ID_DE_CONCEPTO = '174' 
     OR HISTORICOS.ID_DE_CONCEPTO = '175' 
     OR HISTORICOS.ID_DE_CONCEPTO = '404' 
     OR HISTORICOS.ID_DE_CONCEPTO = '092'
     OR HISTORICOS.ID_DE_CONCEPTO = '166')
GROUP BY PERSONAL.ID_DE_EMPLEADO,
    PERSONAL.NOMBRECOMPLETO,  
    CARGOS.NOMBRE_DEL_CARGO,
    PERSONAL.NUMERO_DCTO, 
    PERSONAL.FECHA_DE_INGRESO,
    PERSONAL.FECHA_INICIO, 
    DEPENDENCIA.NOMBRE,
    PERSONAL.FECHA_DE_RETIRO, 
    PERSONAL.ESTADO_ACTUAL, 
    CENTRO_COSTO.NOMBRE,
    PERSONAL.DE_CARRERA
HAVING SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='404' THEN HISTORICOS.VALOR ELSE 0 END)<>0
ORDER BY PERSONAL.NOMBRECOMPLETO,
    CARGOS.NOMBRE_DEL_CARGO