MERGE INTO CONSULTAS FIN USING (SELECT '002126RESUMENAUTOPENSIONIDI' INFORME ,TO_CLOB(q'[SELECT 
  PERSONAL_HISTORICO.NOMBRE_FONDOPENSION, 
  V_FONDO_DE_PENSIONES.NIT NITFONDO,   
  SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=112 THEN V_ACUMULADOS.VALOR ELSE 0 END)  IBC, 
  SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=131 THEN V_ACUMULADOS.VALOR ELSE 0 END)   PENSIONEMPLEADO, 
  SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=117 THEN V_ACUMULADOS.VALOR ELSE 0 END)   PENSIONPATRONO, 
  SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=132 THEN V_ACUMULADOS.VALOR ELSE 0 END)   SOLIDARIDAD, 
  SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=120 THEN V_ACUMULADOS.VALOR ELSE 0 END)   ADICIONALSOLIDARIDAD
FROM V_ACUMULADOS 
  INNER JOIN PERSONAL_HISTORICO 
     ON  V_ACUMULADOS.COMPANIA = PERSONAL_HISTORICO.COMPANIA
    AND  V_ACUMULADOS.ID_DE_PROCESO = PERSONAL_HISTORICO.ID_DE_PROCESO
    AND  V_ACUMULADOS.ANO = PERSONAL_HISTORICO.ANO
    AND  V_ACUMULADOS.MES = PERSONAL_HISTORICO.MES
    AND  V_ACUMULADOS.PERIODO = PERSONAL_HISTORICO.PERIODO
    AND  V_ACUMULADOS.ID_DE_EMPLEADO = PERSONAL_HISTORICO.ID_DE_EMPLEADO
  INNER JOIN V_FONDO_DE_PENSIONES
     ON PERSONAL_HISTORICO.COMPANIA = V_FONDO_DE_PENSIONES.COMPANIA
    AND PERSONAL_HISTORICO.ID_DEL_FONDO = V_FONDO_DE_PENSIONES.ID_DEL_FONDO
WHERE V_ACUMULADOS.COMPANIA = s$compania$s
  AND V_ACUMULADOS.ID_DE_PROCESO = s$idProceso$s
  AND V_ACUMULADOS.ANO = s$ano$s
  AND V_ACUMULADOS.MES = s$rangoMes$s
  AND V_ACUMULADOS.PERIODO = s$periodo$s  
  GROUP BY PERSONAL_HISTORICO.NOMBRE_FONDOPENSION,
        V_FONDO_DE_PENSIONES.NIT]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);