SELECT 
	HISTORICOS.ANO,
	TO_CHAR(HISTORICOS.FECHA, 'Month','nls_date_language=spanish') AS MES,
	PERSONAL.ID_CENTRO_DE_COSTO,
    TRIM (NOMBRES) 
    || ' ' 
    || (APELLIDO1) 
    || ' ' 
    || (APELLIDO2) AS NOMBRE_EMPLEADO,
	CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO, 
	CONCEPTOS.CLASE,
    CONCEPTOS.CLASE AS CLASE,
	CASE CONCEPTOS.CLASE
	    WHEN 3 THEN 'DEVENGOS'
		WHEN 4 THEN 'DEVENGADO'
		WHEN 5 THEN 'DESCUENTO'
		WHEN 6 THEN 'DEDUCCIONES'
		WHEN 7 THEN 'NETO A PAGAR'
		ELSE ' '
	END AS NOMBRE_CLASE,
	HISTORICOS.ID_DE_CONCEPTO,
	CONCEPTOS.NOMBRE_CONCEPTO,
	PERSONAL.NUMERO_DCTO,
	PERSONAL.DCTO_IDENTIDAD,
	SUM(HISTORICOS.VALOR) AS SUMADEVALOR,
	SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '144' THEN HISTORICOS.VALOR ELSE 0 END) AS NETO_PAGAR,
	SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '97' THEN HISTORICOS.VALOR ELSE 0 END) AS NETO_DEVENGADO,
	SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '140' THEN HISTORICOS.VALOR ELSE 0 END) AS NETO_DEDUCCIONES
	FROM 
	HISTORICOS
	INNER JOIN CONCEPTOS
		ON HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA
		AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
	INNER JOIN PERSONAL
		ON HISTORICOS.COMPANIA = PERSONAL.COMPANIA
		AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
	INNER JOIN CENTRO_COSTO
		ON PERSONAL.ANO = CENTRO_COSTO.ANO
		AND PERSONAL.COMPANIA = CENTRO_COSTO.COMPANIA
		AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
WHERE
	HISTORICOS.ID_DE_PROCESO = s$proceso$s
	AND HISTORICOS.ANO = s$ano$s
	AND HISTORICOS.MES = s$mes$s
	AND HISTORICOS.PERIODO = s$periodo$s
        AND CONCEPTOS.CLASE IN('3','5','4','6','7')
GROUP BY 
	PERSONAL.NOMBRECOMPLETO,
	HISTORICOS.ANO,
	HISTORICOS.FECHA,
	PERSONAL.ID_CENTRO_DE_COSTO,
     	TRIM (NOMBRES) 
    	|| ' ' 
    	|| (APELLIDO1) 
   	|| ' ' 
    	|| (APELLIDO2),
	CENTRO_COSTO.NOMBRE, 
	CONCEPTOS.CLASE,
	HISTORICOS.ID_DE_CONCEPTO,
	CONCEPTOS.NOMBRE_CONCEPTO,
	PERSONAL.NUMERO_DCTO,
	PERSONAL.DCTO_IDENTIDAD
ORDER BY 
	PERSONAL.ID_CENTRO_DE_COSTO,
	CONCEPTOS.CLASE