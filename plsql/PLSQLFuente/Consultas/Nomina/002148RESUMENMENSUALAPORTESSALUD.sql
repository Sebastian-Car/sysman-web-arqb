SELECT 
	CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO, 
	DEPENDENCIA.CODIGO AS DEPENDENCIA,
	DEPENDENCIA.NOMBRE,
	HISTORICOS.ANO, 
	TO_CHAR(HISTORICOS.FECHA, 'Month','nls_date_language=spanish') AS MES,
	HISTORICOS.PERIODO,
	PERSONAL.ID_CENTRO_DE_COSTO, 
	V_FONDO_DE_SALUD.NIT, 
	V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD, 
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '134' THEN HISTORICOS.VALOR ELSE 0 END) AS BASE_COTIZACION, 
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '113' THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE_SALUD_EMPLEADO, 
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '116' THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE_SALUD_PATRONO, 
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '139' THEN HISTORICOS.VALOR ELSE 0 END) AS INCAPACIDAD, 
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '123' THEN HISTORICOS.VALOR ELSE 0 END) AS UPC_ADICIONAL
FROM 
	HISTORICOS
	INNER JOIN CONCEPTOS
		ON HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA
		AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
	INNER JOIN PERSONAL
		ON HISTORICOS.COMPANIA = PERSONAL.COMPANIA
		AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
	INNER JOIN DEPENDENCIA
		ON PERSONAL.COMPANIA = DEPENDENCIA.COMPANIA
		AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
	INNER JOIN CENTRO_COSTO
		ON PERSONAL.ANO = CENTRO_COSTO.ANO
		AND PERSONAL.COMPANIA = CENTRO_COSTO.COMPANIA
		AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
	LEFT JOIN V_FONDO_DE_SALUD 
		ON PERSONAL.COMPANIA = V_FONDO_DE_SALUD.COMPANIA
		AND PERSONAL.FONDO_SALUD = V_FONDO_DE_SALUD.FONDO_SALUD
WHERE
	HISTORICOS.ANO = s$ano$s 
	AND HISTORICOS.MES = s$mes$s 
	AND HISTORICOS.PERIODO = s$periodo$s
	AND HISTORICOS.ID_DE_PROCESO = s$proceso$s
GROUP BY 
	CENTRO_COSTO.NOMBRE, 
	DEPENDENCIA.CODIGO,
	DEPENDENCIA.NOMBRE,
	HISTORICOS.ANO, 
	HISTORICOS.FECHA,
	HISTORICOS.PERIODO,
	PERSONAL.ID_CENTRO_DE_COSTO, 
	V_FONDO_DE_SALUD.NIT, 
	V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD
ORDER BY
	PERSONAL.ID_CENTRO_DE_COSTO, 
	V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD
