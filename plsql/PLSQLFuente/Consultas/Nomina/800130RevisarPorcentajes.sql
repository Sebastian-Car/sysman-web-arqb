SELECT PERSONAL.ESTADO_ACTUAL,
  PERSONAL.APELLIDO1,
  PERSONAL.APELLIDO2,
  PERSONAL.NOMBRES,
  NOVEDADES.OBSERVACIONES,
  PERSONAL.PROCESORETENCION,
  NOVEDADES.VALOR,
  PERSONAL.ID_DE_EMPLEADO,
  TO_CHAR(PERSONAL.FECHA_DE_INGRESO, 'DD/MM/YYYY') FECHA_INGRESO
FROM PERSONAL
  INNER JOIN NOVEDADES
    ON PERSONAL.ID_DE_EMPLEADO  = NOVEDADES.ID_DE_EMPLEADO
   AND PERSONAL.COMPANIA        = NOVEDADES.COMPANIA
WHERE PERSONAL.COMPANIA        = s$compania$s 
  AND PERSONAL.ESTADO_ACTUAL   NOT IN (3)
  AND NOVEDADES.ID_DE_CONCEPTO IN (303)
ORDER BY PERSONAL.APELLIDO1,
  PERSONAL.APELLIDO2,
  PERSONAL.NOMBRES
