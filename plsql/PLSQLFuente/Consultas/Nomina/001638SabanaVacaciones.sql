SELECT 
	PERIODOS.COMPANIA, 
	PERIODOS.ID_DE_PROCESO, 
	PERIODOS.ANO, 
	PERIODOS.MES, 
	PERIODOS.PERIODO, 
	PERSONAL.ID_DE_EMPLEADO, 
	PERSONAL.NUMERO_DCTO, 
	(PERSONAL.APELLIDO1 || ' ' || PERSONAL.APELLIDO2 || ' ' || PERSONAL.NOMBRES) NOMBRECOMPLETO, 
	PERSONAL.FECHA_DE_INGRESO, 
	CARGOS.NOMBRE_DEL_CARGO, 
	CATEGORIA.SALARIO_BASE SALARIO_BASE, 
	VACACIONES.FECHA_INICIO, 
	VACACIONES.FECHA_FINAL, 
	VACACIONES.INICIO_DISFRUTE, 
	VACACIONES.FINAL_DISFRUTE, 
	VACACIONES.DIAS, 
	VACACIONES.DIASHABILES, 
	PERIODOS.FECHAFINAL
FROM VACACIONES 
   INNER JOIN PERSONAL 
	  ON VACACIONES.COMPANIA       = PERSONAL.COMPANIA 
	 AND VACACIONES.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO 
   INNER JOIN CATEGORIA 
	  ON PERSONAL.COMPANIA 		  = CATEGORIA.COMPANIA 
	 AND PERSONAL.ESCALAFON       = CATEGORIA.ESCALAFON
	 AND PERSONAL.ID_DE_CATEGORIA = CATEGORIA.ID_DE_CATEGORIA
	 AND PERSONAL.ANO  			  = CATEGORIA.ANO
   INNER JOIN CARGOS 
	  ON PERSONAL.COMPANIA 		= CARGOS.COMPANIA 
	 AND PERSONAL.ID_DE_CARGO 	= CARGOS.ID_DE_CARGO 
	LEFT JOIN PERIODOS 
	 ON VACACIONES.COMPANIA 	 = PERIODOS.COMPANIA
	AND VACACIONES.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO
	AND VACACIONES.ANO 			 = PERIODOS.ANO 
	AND VACACIONES.MES 			 = PERIODOS.MES 
	AND VACACIONES.FECHAPAGO 	 = PERIODOS.FECHAFINAL 
WHERE PERIODOS.COMPANIA    = s$compania$s 
AND PERIODOS.ID_DE_PROCESO = s$procesoActual$s 
AND PERIODOS.ANO = s$ano1$s 
AND PERIODOS.MES = s$mes1$s 
AND PERIODOS.FECHAFINAL =  PCK_NOMINA.FC_FECHAPERIODO(UN_COMPANIA => s$compania$s,   
				     	    UN_PROCESO  => s$procesoActual$s, 
					    UN_ANO  	  => s$ano1$s,
					    UN_MES 	  => s$mes1$s,
					    UN_PERIODO  => s$periodo1$s)  
ORDER BY PERIODOS.PERIODO, PERSONAL.ID_DE_EMPLEADO
