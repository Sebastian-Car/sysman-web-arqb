CONCEPTOS.NOMBRE_CONCEPTO,
(CASE WHEN PERSONAL.GRUPOCONTABLE = 'A'  THEN 'ADMINISTRACION' ELSE 
    CASE WHEN PERSONAL.GRUPOCONTABLE = 'V' THEN 'VENTAS' ELSE 'PRODUCCION' END END) GRUPOCONTABLE,
CONCEPTOS.CLASE,
SUM(HISTORICOS.VALOR) AS SUMA_DE_VALOR,
CASE WHEN CONCEPTOS.CLASE = 3 THEN 'VALOR DEVENGOS' ELSE
    CASE WHEN CONCEPTOS.CLASE = 5 THEN 'VALOR DESCUENTOS' ELSE
        CASE WHEN CONCEPTOS.CLASE = 4 THEN 'VALOR TOTAL DEVENGOS' ELSE 
             CASE WHEN CONCEPTOS.CLASE = 6 THEN 'VALOR TOTAL DESCUENTOS' ELSE 
                CASE WHEN CONCEPTOS.CLASE = 7 THEN 'VALOR NETO A PAGAR' ELSE 'CONCEPTOS CONTABLES'
                END
            END
        END
    END
END CLASECONCEPTO,
DEPENDENCIA.NOMBRE,
PERSONAL.DEPENDENCIA

