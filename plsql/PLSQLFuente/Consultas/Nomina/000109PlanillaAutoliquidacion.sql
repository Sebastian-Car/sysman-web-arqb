SELECT V_ACUMULADOS.ID_DE_EMPLEADO  CODIGO, 
      PERSONAL_HISTORICO.ID_DE_CARGO CARGO, 
      PERSONAL_HISTORICO.NOMBRE_DE_CARGO NOMBRECARGO, 
      PERSONAL_HISTORICO.DEPENDENCIA DEPENDENCIA, 
      PERSONAL_HISTORICO.NOMBRE_DEPENDENCIA NOMBREDEPENDENCIA, 
      PERSONAL_HISTORICO.NOMBRECOMPLETO NOM_EMPLEADO, 
      PERSONAL_HISTORICO.NUMERO_DCTO CEDULA, 
      V_ACUMULADOS.PERIODO, 
      PCK_SYSMAN_UTL.FC_NOMBRE_MES(V_ACUMULADOS.MES) MES,
      V_ACUMULADOS.ANO,
      V_ACUMULADOS.ID_DE_PROCESO PROCESO,
      SUM(V_ACUMULADOS.DEVENGOS) DEVENGOS, 
      SUM(V_ACUMULADOS.DESCUENTOS) DESCUENTOS,
      SUM(V_ACUMULADOS.NETOAPAGAR) NETO,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO =   9 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C009,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO =   1 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C001,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO =   2 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C002,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO =  70 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C070, 
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 174 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C174,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 350 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C350,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 351 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C351,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 352 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C352,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 353 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C353,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 354 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C354,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 370 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C370,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 371 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C371,  
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 372 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C372,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 373 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C373,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 374 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C374,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 112 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C112,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 118 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C118,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 124 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C124,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 113 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C113,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 123 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C123,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 115 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C115,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 117 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C117,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 116 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS C116
FROM  V_ACUMULADOS INNER JOIN PERSONAL_HISTORICO
   ON V_ACUMULADOS.COMPANIA       = PERSONAL_HISTORICO.COMPANIA
  AND V_ACUMULADOS.ID_DE_PROCESO  = PERSONAL_HISTORICO.ID_DE_PROCESO
  AND V_ACUMULADOS.ANO            = PERSONAL_HISTORICO.ANO
  AND V_ACUMULADOS.MES            = PERSONAL_HISTORICO.MES
  AND V_ACUMULADOS.PERIODO        = PERSONAL_HISTORICO.PERIODO
  AND V_ACUMULADOS.ID_DE_EMPLEADO = PERSONAL_HISTORICO.ID_DE_EMPLEADO
WHERE V_ACUMULADOS.COMPANIA        =   s$compania$s
  AND V_ACUMULADOS.ID_DE_PROCESO   =   s$procesoNomina$s
  AND V_ACUMULADOS.ANO             =   s$anioNomina$s
  AND V_ACUMULADOS.MES             =   s$mesNomina$s
  AND V_ACUMULADOS.PERIODO         =   s$periodoNomina$s
  AND PERSONAL_HISTORICO.ESTADO_ACTUAL     <>  3  
GROUP BY V_ACUMULADOS.ID_DE_EMPLEADO,
        PERSONAL_HISTORICO.ID_DE_CARGO, 
        PERSONAL_HISTORICO.NOMBRE_DE_CARGO, 
        PERSONAL_HISTORICO.DEPENDENCIA, 
        PERSONAL_HISTORICO.NOMBRE_DEPENDENCIA, 
        PERSONAL_HISTORICO.NOMBRECOMPLETO, 
        PERSONAL_HISTORICO.NUMERO_DCTO,
        V_ACUMULADOS.PERIODO,
        V_ACUMULADOS.MES,
        V_ACUMULADOS.ANO,
        V_ACUMULADOS.ID_DE_PROCESO
