
MERGE INTO CONSULTAS FIN USING (SELECT '002360SubinformeConsultaFinanciablesPCastilla' INFORME ,TO_CLOB(q'[SELECT CONCEPTOS.NOMBRE_CONCEPTO, 
     FINANCIABLES_DE_NOMINA.SALDO, 
     FINANCIABLES_DE_NOMINA.SALDO AS SALDOFINAL 
FROM FINANCIABLES_DE_NOMINA 
INNER JOIN CONCEPTOS  
  ON FINANCIABLES_DE_NOMINA.COMPANIA 		= CONCEPTOS.COMPANIA 
 AND FINANCIABLES_DE_NOMINA.ID_DE_CONCEPTO 	= CONCEPTOS.ID_DE_CONCEPTO  
WHERE FINANCIABLES_DE_NOMINA.COMPANIA       = $P{PR_COMPANIA} 
  AND FINANCIABLES_DE_NOMINA.ID_DE_PROCESO  = $P{PR_ID_DE_PROCESO} 
  AND FINANCIABLES_DE_NOMINA.ANO            = $P{PR_ANO} 
  AND FINANCIABLES_DE_NOMINA.MES            = $P{PR_MES} 
  AND FINANCIABLES_DE_NOMINA.PERIODO        = $P{PR_PERIODO} 
  AND FINANCIABLES_DE_NOMINA.ID_DE_EMPLEADO = $P{PR_ID_DE_EMPLEADO}]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);



MERGE INTO CONSULTAS_SUB FIN USING (SELECT '002359VOLANTESUNOCASTILLA' INFORME_PADRE,
	'002360SubinformeConsultaFinanciablesPCastilla' INFORME_HIJO, 
	'PR_STRSQL_CONSULTAFINANCIABLES' PARAMETRO ,
	NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL
) INI ON (FIN.INFORME_PADRE = INI.INFORME_PADRE 
	AND INI.INFORME_HIJO = FIN.INFORME_HIJO
)  WHEN MATCHED THEN  UPDATE SET FIN.PARAMETRO = INI.PARAMETRO ,
 FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE 
  WHEN NOT MATCHED THEN  INSERT (INFORME_PADRE,INFORME_HIJO, PARAMETRO,CREATED_BY,DATE_CREATED)
    VALUES (INI.INFORME_PADRE,INI.INFORME_HIJO, INI.PARAMETRO,INI.CREATED_BY,SYSDATE);