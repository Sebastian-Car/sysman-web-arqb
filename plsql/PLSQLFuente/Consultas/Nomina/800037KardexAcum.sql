SELECT * FROM( 
   SELECT * FROM(
     SELECT 
       SUM(V_ACUMULADOS.VALOR) OVER (PARTITION BY V_ACUMULADOS.ID_DE_CONCEPTO) TOTALT,
       V_ACUMULADOS.ID_DE_CONCEPTO AS CONCEPTO, 
       V_ACUMULADOS.NOMBRE_CONCEPTO AS s$ENCABEZADOEMPLEADO$s,  
       V_ACUMULADOS.KARDEX, 
       V_ACUMULADOS.VALOR AS TOTAL,  
       V_ACUMULADOS.ANO ||'_'|| LPAD(V_ACUMULADOS.MES,2,'0') ||'_'|| LPAD(V_ACUMULADOS.PERIODO,2,'0') ||'_'|| PCK_SYSMAN_UTL.FC_NOMBRE_MES(V_ACUMULADOS.MES) PIV
      FROM V_ACUMULADOS
     WHERE V_ACUMULADOS.COMPANIA= s$compania$s
       AND V_ACUMULADOS.ID_DE_EMPLEADO= s$IDEMPLEADO$s 
       AND V_ACUMULADOS.CLASE IN (1,3,8,4,99,6,7,5) 
       AND V_ACUMULADOS.ANO || LPAD(V_ACUMULADOS.MES,2,'0') || LPAD(V_ACUMULADOS.PERIODO,2,'0') 
       BETWEEN s$DESDE$s 
       AND s$HASTA$s
       AND V_ACUMULADOS.KARDEX<>0
       ORDER BY V_ACUMULADOS.ANO ||'_'|| LPAD(V_ACUMULADOS.MES,2,'0') ||'_'|| LPAD(V_ACUMULADOS.PERIODO,2,'0') ||'_'|| PCK_SYSMAN_UTL.FC_NOMBRE_MES(V_ACUMULADOS.MES)
       ) 
       PIVOTAUX
       PIVOT(SUM(TOTAL) FOR PIV IN 
       ( s$CONDICIONPIVOT$s )
       )
     ) 
     ORDER BY CONCEPTO
