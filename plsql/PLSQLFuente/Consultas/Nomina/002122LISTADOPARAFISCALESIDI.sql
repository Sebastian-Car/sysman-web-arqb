SELECT 
  HISTORICOS.COMPANIA,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  BASEPARAFISCALES_STR.ID_DE_CONCEPTO,
  BASEPARAFISCALES_STR.NOMBRE_CONCEPTO,
  BASEPARAFISCALES_STR.FACTOR,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='101' THEN VALOR ELSE 0 END ) AS COMPENSACION,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='102' THEN VALOR ELSE 0 END ) AS BIENESTAR,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='103' THEN VALOR ELSE 0 END )  AS SENA,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='104' THEN VALOR ELSE 0 END )  AS ESAP,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='105' THEN VALOR ELSE 0 END )  AS INSTITUTOS,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='108' THEN VALOR ELSE 0 END )  AS BASESP,
  TO_NUMBER(PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA,45)) AS PARA45,
  TO_NUMBER(PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA,46)) AS PARA46,
  TO_NUMBER(PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA,47)) AS PARA47,
  TO_NUMBER(PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA,48)) AS PARA48,
  TO_NUMBER(PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA,49)) AS PARA49,
  PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES) AS MESP
FROM HISTORICOS
  INNER JOIN  (SELECT 
                HISTORICOS.COMPANIA,
                HISTORICOS.ID_DE_PROCESO,
                HISTORICOS.ANO,
                HISTORICOS.MES,
                SUM(HISTORICOS.VALOR) FACTOR,
                CONCEPTOS.FACTOR_PARAFISCAL,
                CONCEPTOS.NOMBRE_CONCEPTO,
                HISTORICOS.ID_DE_CONCEPTO
              FROM HISTORICOS 
                INNER JOIN CONCEPTOS
                  ON  HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA
                  AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
                INNER JOIN PERSONAL 
                  ON  HISTORICOS.COMPANIA = PERSONAL.COMPANIA 
                  AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
                INNER JOIN PERIODOS
                  ON  HISTORICOS.COMPANIA = PERIODOS.COMPANIA
                  AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO 
                  AND  HISTORICOS.ANO = PERIODOS.ANO
                  AND HISTORICOS.MES = PERIODOS.MES
                  AND HISTORICOS.PERIODO = PERIODOS.PERIODO
              WHERE  HISTORICOS.COMPANIA=s$compania$s 
                AND HISTORICOS.ANO=s$ano1$s  
                AND HISTORICOS.MES=s$mes1$s  
                AND CONCEPTOS.FACTOR_PARAFISCAL<>0 
                AND PERSONAL.ESTADO_ACTUAL<>2
                AND PERSONAL.ID_DE_TIPO NOT IN ('99','04','05') 
                AND PERIODOS.ACUMULADO<>0
              GROUP BY 
                HISTORICOS.COMPANIA, HISTORICOS.ID_DE_PROCESO, HISTORICOS.ANO, HISTORICOS.MES, 
                CONCEPTOS.FACTOR_PARAFISCAL, CONCEPTOS.NOMBRE_CONCEPTO, HISTORICOS.ID_DE_CONCEPTO) BASEPARAFISCALES_STR
    ON  HISTORICOS.COMPANIA = BASEPARAFISCALES_STR.COMPANIA
    AND HISTORICOS.ID_DE_PROCESO = BASEPARAFISCALES_STR.ID_DE_PROCESO
    AND HISTORICOS.ANO = BASEPARAFISCALES_STR.ANO
    AND HISTORICOS.MES = BASEPARAFISCALES_STR.MES
  INNER JOIN PERSONAL 
    ON  HISTORICOS.COMPANIA = PERSONAL.COMPANIA
    AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO 
WHERE HISTORICOS.COMPANIA=s$compania$s 
  AND HISTORICOS.ANO=s$ano1$s
  AND HISTORICOS.MES=s$mes1$s
  AND HISTORICOS.ID_DE_CONCEPTO IN('101','102','103','104','105','108')
  AND PERSONAL.ESTADO_ACTUAL<>2
GROUP BY 
  HISTORICOS.COMPANIA, HISTORICOS.ID_DE_PROCESO, HISTORICOS.ANO, HISTORICOS.MES, BASEPARAFISCALES_STR.ID_DE_CONCEPTO, BASEPARAFISCALES_STR.NOMBRE_CONCEPTO,
  BASEPARAFISCALES_STR.FACTOR, TO_NUMBER(PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA,45)), TO_NUMBER(PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA,46)),
  TO_NUMBER(PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA,47)), TO_NUMBER(PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA,48)),
  TO_NUMBER(PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA,49)), PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES) 