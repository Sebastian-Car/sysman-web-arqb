WITH CONSULTA AS (SELECT HISTORICOS.ID_DE_PROCESO,
    HISTORICOS.ANO,
    HISTORICOS.MES,
    HISTORICOS.ID_DE_EMPLEADO,
    SUM(
    CASE
      WHEN CONCEPTOS.CLASE = 3
      THEN HISTORICOS.VALOR
      ELSE 0
    END) AS DEVENGOS,
    SUM(
    CASE
      WHEN CONCEPTOS.CLASE = 5
      THEN HISTORICOS.VALOR
      ELSE 0
    END) AS DESCUENTOS,
    SUM(
    CASE
      WHEN CONCEPTOS.CLASE = 7
      THEN HISTORICOS.VALOR
      ELSE 0
    END) AS NETOHIS,
    TO_CHAR(MAX(HISTORICOS.FECHA) KEEP(DENSE_RANK FIRST ORDER BY ROWNUM), 'DD/MM/YYYY') PRIMERO_DE_FECHA,
    PERSONAL.ESTADO_ACTUAL,
    PERSONAL.NIVELSIIF,
    PERSONAL.ID_CENTRO_DE_COSTO,
    PERSONAL.ID_DE_CARGO,
    PERSONAL.APELLIDO1,
    PERSONAL.APELLIDO2,
    PERSONAL.NOMBRES
  FROM HISTORICOS
  LEFT JOIN CONCEPTOS
  ON HISTORICOS.COMPANIA        = CONCEPTOS.COMPANIA
  AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
  INNER JOIN PERSONAL
  ON HISTORICOS.COMPANIA        = PERSONAL.COMPANIA
  AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
  WHERE    HISTORICOS.COMPANIA  = 's$compania$s'
  AND HISTORICOS.ANO            = s$anio$s
  AND HISTORICOS.MES            = s$mes$s
  AND HISTORICOS.PERIODO        = s$periodo$s
  AND CONCEPTOS.CLASE        IN (3,5,7)
  GROUP BY HISTORICOS.ID_DE_PROCESO,
    HISTORICOS.ANO,
    HISTORICOS.MES,
    HISTORICOS.ID_DE_EMPLEADO,
    PERSONAL.ESTADO_ACTUAL,
    PERSONAL.NIVELSIIF,
    PERSONAL.ID_CENTRO_DE_COSTO,
    PERSONAL.ID_DE_CARGO,
    PERSONAL.APELLIDO1,
    PERSONAL.APELLIDO2,
    PERSONAL.NOMBRES) 
SELECT CONSULTA.*,
  CONSULTA.DEVENGOS        -CONSULTA.DESCUENTOS                                   AS NETO,
  ROUND((CONSULTA.DEVENGOS - CONSULTA.DESCUENTOS), 0) - ROUND(CONSULTA.NETOHIS,0) AS DIFER
FROM CONSULTA
