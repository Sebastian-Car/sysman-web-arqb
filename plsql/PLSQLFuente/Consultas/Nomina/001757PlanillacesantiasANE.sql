MERGE INTO CONSULTAS FIN USING (SELECT '001757PlanillacesantiasANE' INFORME ,TO_CLOB(q'[SELECT PERSONAL.ID_DE_EMPLEADO,
       PERSONAL.NOMBRECOMPLETO  NOMCOMPLETO, 
       PERSONAL.NUMERO_DCTO, 
       PERSONAL.FECHA_DE_INGRESO, 
       PERSONAL.FECHAFONDOCESANTIA, 
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('910') THEN HISTORICOS.VALOR ELSE 0 END) DT,
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('912') THEN HISTORICOS.VALOR ELSE 0 END) DNT,
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('913') THEN HISTORICOS.VALOR ELSE 0 END) BASE,
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('277') THEN HISTORICOS.VALOR ELSE 0 END)+ 
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('483') THEN HISTORICOS.VALOR ELSE 0 END)+ 
       SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('484') THEN HISTORICOS.VALOR ELSE 0 END) CES,
       V_FONDO_DE_CESANTIAS.NIT, 
       V_FONDO_DE_CESANTIAS.NOMBRE_FONDO_CESANTIAS
FROM HISTORICOS
	INNER JOIN PERIODOS 
		ON HISTORICOS.COMPANIA 			= PERIODOS.COMPANIA
		AND HISTORICOS.ID_DE_PROCESO 	= PERIODOS.ID_DE_PROCESO
		AND HISTORICOS.ANO 				= PERIODOS.ANO
		AND HISTORICOS.MES 				= PERIODOS.MES
		AND HISTORICOS.PERIODO 			= PERIODOS.PERIODO
	INNER JOIN PERSONAL
		ON HISTORICOS.COMPANIA 			= PERSONAL.COMPANIA 
	    AND HISTORICOS.ID_DE_EMPLEADO 	= PERSONAL.ID_DE_EMPLEADO 
	INNER JOIN V_FONDO_DE_CESANTIAS
	    ON PERSONAL.COMPANIA 		    = V_FONDO_DE_CESANTIAS.COMPANIA
	    AND PERSONAL.FONDO_CESANTIAS    = V_FONDO_DE_CESANTIAS.FONDO_CESANTIAS
WHERE HISTORICOS.COMPANIA = s$compania$s
	AND HISTORICOS.ANO = s$anio$s 
	AND HISTORICOS.MES = s$mes$s 
	AND PERSONAL.FONDO_CESANTIAS = s$fondoCesantias$s   
	AND HISTORICOS.ID_DE_CONCEPTO IN(277,910,911,912,913,269,483,484)
	AND 
	CASE WHEN  s$acumulado$s = 1
	     THEN  PERIODOS.ACUMULADO 
	     ELSE  1
    END NOT IN(0)	
GROUP BY PERSONAL.ID_DE_EMPLEADO,
       PERSONAL.NOMBRECOMPLETO  , 
       PERSONAL.NUMERO_DCTO, 
       PERSONAL.FECHA_DE_INGRESO, 
       PERSONAL.FECHAFONDOCESANTIA, 
        V_FONDO_DE_CESANTIAS.NIT, 
        V_FONDO_DE_CESANTIAS.NOMBRE_FONDO_CESANTIAS
HAVING 	SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('277') 
				THEN HISTORICOS.VALOR ELSE 0 END)+
		SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('483') 
				THEN HISTORICOS.VALOR ELSE 0 END)+
		SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('484') 
				THEN HISTORICOS.VALOR ELSE 0 END) NOT IN (0)]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
