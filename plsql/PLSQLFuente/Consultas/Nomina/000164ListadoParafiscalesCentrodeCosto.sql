SELECT HISTORICOS.ID_DE_PROCESO, 
       HISTORICOS.ANO,  
       PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES) AS MES,  
       SUM(( CASE WHEN HISTORICOS.ID_DE_CONCEPTO =108 THEN  HISTORICOS.VALOR  ELSE 0 END )) AS FACTOR,  
       SUM(( CASE WHEN HISTORICOS.ID_DE_CONCEPTO =101 THEN  HISTORICOS.VALOR  ELSE 0 END )) AS COMPENSACION, 
       SUM(( CASE WHEN HISTORICOS.ID_DE_CONCEPTO =102 THEN  HISTORICOS.VALOR  ELSE 0 END )) AS BIENESTAR,  
       SUM(( CASE WHEN HISTORICOS.ID_DE_CONCEPTO =103 THEN  HISTORICOS.VALOR  ELSE 0 END )) AS SENA,  
       SUM(( CASE WHEN HISTORICOS.ID_DE_CONCEPTO =104 THEN  HISTORICOS.VALOR  ELSE 0 END )) AS ESAP,  
       SUM(( CASE WHEN HISTORICOS.ID_DE_CONCEPTO =105 THEN  HISTORICOS.VALOR  ELSE 0 END )) AS INSTITUTOS,  
       PERSONAL_HISTORICO.ID_CENTRO_DE_COSTO CODIGO,  
       PERSONAL_HISTORICO.NOMBRE_CENTRO_DE_COSTO NOMBRE, 
       PCK_SYSMAN_UTL.FC_ROUND( 
       (SUM(( CASE WHEN HISTORICOS.ID_DE_CONCEPTO =108 THEN  HISTORICOS.VALOR  ELSE 0 END ))*9/100)- 
	   (SUM(( CASE WHEN HISTORICOS.ID_DE_CONCEPTO =101 THEN  HISTORICOS.VALOR  ELSE 0 END )) + 
	    SUM(( CASE WHEN HISTORICOS.ID_DE_CONCEPTO =102 THEN  HISTORICOS.VALOR  ELSE 0 END )) + 
      SUM(( CASE WHEN HISTORICOS.ID_DE_CONCEPTO =103 THEN  HISTORICOS.VALOR  ELSE 0 END )) + 
      SUM(( CASE WHEN HISTORICOS.ID_DE_CONCEPTO =105 THEN  HISTORICOS.VALOR  ELSE 0 END )) +  
      SUM(( CASE WHEN HISTORICOS.ID_DE_CONCEPTO =104 THEN  HISTORICOS.VALOR  ELSE 0 END ))),0)  AS AJUSTECALCULO 
FROM HISTORICOS INNER JOIN PERSONAL_HISTORICO 
	 ON HISTORICOS.COMPANIA 		    = PERSONAL_HISTORICO.COMPANIA   
	AND HISTORICOS.ID_DE_EMPLEADO 	= PERSONAL_HISTORICO.ID_DE_EMPLEADO 
WHERE HISTORICOS.COMPANIA		    = s$compania$s 
  AND HISTORICOS.ID_DE_PROCESO	= s$procesoNomina$s 
  AND HISTORICOS.ANO			      =s$anioNomina$s 
  AND HISTORICOS.MES			      =s$mesNomina$s 
  AND HISTORICOS.ID_DE_CONCEPTO IN (101 ,102,103,104,105,108) 
GROUP BY HISTORICOS.ID_DE_PROCESO,  
		 HISTORICOS.ANO,  
		 HISTORICOS.MES,  
		 PERSONAL_HISTORICO.ID_CENTRO_DE_COSTO,  
		 PERSONAL_HISTORICO.NOMBRE_CENTRO_DE_COSTO
