PERSONAL.NOMBRECOMPLETO, 
DISTPERSONALDIARIO.ID_DE_PROCESO, 
DISTPERSONALDIARIO.ANO, 
DISTPERSONALDIARIO.MES, 
DISTPERSONALDIARIO.PERIODO, 
DISTPERSONALDIARIO.FECHA, 
DISTPERSONALDIARIO.ID_CENTRO_DE_COSTO, 
DISTPERSONALDIARIO.HORAST, 
