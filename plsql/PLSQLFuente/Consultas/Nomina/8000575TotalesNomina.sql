  NUMERO_DCTO                       CEDULA
 ,NOMBRECOMPLETO                    NOMBRE_EMPLEADO
 ,ID_DE_CARGO  || ' -' || NOMBRE_DE_CARGO   CARGO
 ,SUM (DEVENGOS )                   DEVENGOS
 ,SUM(DESCUENTOS)                   DEDUCCIONES
 ,(SUM(DEVENGOS) - SUM(DESCUENTOS)) NETO_A_PAGAR
  ,SALARIOBASEHISTORICO             SALARIO
FROM V_VOLANTES  
WHERE COMPANIA		 = s$compania$s 
  AND ID_DE_PROCESO  = s$procesoNomina$s 
  AND ANO            = s$anioNomina$s 
  AND MES            = s$mesNomina$s 
  AND PERIODO        = s$periodoNomina$s 
GROUP BY 
  ID_DE_EMPLEADO
 ,ANO
 ,MES
 ,ID_DE_CARGO
 ,NOMBRE_DE_CARGO
 ,PERIODO
 ,NOMBRECOMPLETO
 ,NUMERO_DCTO
 ,SALARIOBASEHISTORICO]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);