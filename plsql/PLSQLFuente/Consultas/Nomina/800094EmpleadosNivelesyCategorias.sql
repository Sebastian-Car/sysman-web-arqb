SELECT 
  ESCALAFON.NOMBRE AS NOMBRE_ESCALAFON, 
  CATEGORIA.ESCALAFON COD_ESCALAFON, 
  CATEGORIA.ID_DE_CATEGORIA COD_CATEGORIA, 
  PERSONAL.ANO, 
  PERSONAL.APELLIDO1, 
  PERSONAL.APELLIDO2, 
  PERSONAL.NOMBRES, 
  PERSONAL.NUMERO_DCTO, 
  CATEGORIA.SALARIO_BASE, 
  PERSONAL.ESTADO_ACTUAL 
FROM PERSONAL  
  INNER JOIN CATEGORIA ON  PERSONAL.COMPANIA = CATEGORIA.COMPANIA  AND PERSONAL.ANO = CATEGORIA.ANO AND PERSONAL.ID_DE_CATEGORIA = CATEGORIA.ID_DE_CATEGORIA AND PERSONAL.ESCALAFON = CATEGORIA.ESCALAFON  
  INNER JOIN ESCALAFON ON  PERSONAL.COMPANIA = ESCALAFON.COMPANIA AND PERSONAL.ESCALAFON = ESCALAFON.CODIGO 
WHERE 
  PERSONAL.COMPANIA = 's$compania$s' 
  AND PERSONAL.ANO =  s$anioNomina$s
  AND PERSONAL.ESTADO_ACTUAL <> 3 
ORDER BY CATEGORIA.ESCALAFON, CATEGORIA.ID_DE_CATEGORIA, PERSONAL.APELLIDO1, PERSONAL.APELLIDO2, PERSONAL.NOMBRES
