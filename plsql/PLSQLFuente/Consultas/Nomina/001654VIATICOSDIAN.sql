  SELECT PERSONAL.APELLIDO1
  || ' '
  || PERSONAL.APELLIDO2
  || ' '
  || PERSONAL.NOMBRES            AS FUNCIONARIO,
  
  SUM(VI_DETALLE_VIATICOS.TOTAL) AS TOTALPAGO,
  
  SUM(VI_DETALLE_VIATICOS.TOTAL+
  CASE
    WHEN VI_CONCEPTO_VIATICOS.TIPO_CONCEPTO='T'
    THEN TOTAL
    ELSE 0
  END )                AS TOTALVIATICOS,
  
  PERSONAL.NUMERO_DCTO AS NUMERO_DCTO,
  ESCALAFON.CODIGO,
  ESCALAFON.NOMBRE,
  CASE
    WHEN VI_CONCEPTO_VIATICOS.TIPO_CONCEPTO='T'
    THEN TOTAL
    ELSE 0
  END AS TRANSPORTE
FROM VI_VIATICOS
INNER JOIN VI_DETALLE_VIATICOS
ON VI_VIATICOS.ANO           = VI_DETALLE_VIATICOS.ANO
AND VI_VIATICOS.COMPANIA     = VI_DETALLE_VIATICOS.COMPANIA
AND VI_VIATICOS.CODSOLICITUD = VI_DETALLE_VIATICOS.NUMERO
AND VI_VIATICOS.TIPO_VIATICO = VI_DETALLE_VIATICOS.TIPO_VIATICO
INNER JOIN PERSONAL
ON VI_DETALLE_VIATICOS.COMPANIA = PERSONAL.COMPANIA
AND VI_DETALLE_VIATICOS.TERCERO = PERSONAL.NUMERO_DCTO
LEFT JOIN DEPENDENCIA
ON PERSONAL.COMPANIA = DEPENDENCIA.COMPANIA
AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
LEFT JOIN ESCALAFON
ON PERSONAL.COMPANIA = ESCALAFON.COMPANIA
AND PERSONAL.ESCALAFON = ESCALAFON.CODIGO
LEFT JOIN VI_CONCEPTO_VIATICOS
ON VI_DETALLE_VIATICOS.COMPANIA  = VI_CONCEPTO_VIATICOS.COMPANIA
AND VI_DETALLE_VIATICOS.ANO      = VI_CONCEPTO_VIATICOS.ANO
AND VI_DETALLE_VIATICOS.CONCEPTO = VI_CONCEPTO_VIATICOS.CODIGO_CONCEPTO
WHERE VI_VIATICOS.COMPANIA       = s$compania$s
AND VI_VIATICOS.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
GROUP BY PERSONAL.APELLIDO1
  || ' '
  || PERSONAL.APELLIDO2
  || ' '
  || PERSONAL.NOMBRES ,
  PERSONAL.NUMERO_DCTO,
  ESCALAFON.CODIGO,
  ESCALAFON.NOMBRE,
    CASE
    WHEN VI_CONCEPTO_VIATICOS.TIPO_CONCEPTO='T'
    THEN TOTAL
    ELSE 0
  END
ORDER BY PERSONAL.APELLIDO1
  || ' '
  || PERSONAL.APELLIDO2
  || ' '
  || PERSONAL.NOMBRES
