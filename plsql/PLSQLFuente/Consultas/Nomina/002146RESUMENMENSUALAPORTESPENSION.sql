SELECT 
	HISTORICOS.ID_DE_PROCESO,
	HISTORICOS.ANO,
	TO_CHAR(HISTORICOS.FECHA, 'MONTH','nls_date_language=spanish') AS MES,
	HISTORICOS.PERIODO, 
	CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO, 
	PERSONAL.ID_CENTRO_DE_COSTO, 
	PERSONAL.ID_DEL_FONDO, 	
	DEPENDENCIA.CODIGO AS DEPENDENCIA, 
	DEPENDENCIA.NOMBRE,
	V_FONDO_DE_PENSIONES.NOMBRE_DEL_FONDO, 
	V_FONDO_DE_PENSIONES.NIT, 
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '134' THEN HISTORICOS.VALOR ELSE 0 END) AS BASE_COTIZACION, 
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '118' THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE_PENSION_EMPLEADO, 
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '117' THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE_PENSION_PATRONO, 
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '115' THEN HISTORICOS.VALOR ELSE 0 END) AS FONDSOLIDARIDAD
FROM 
	HISTORICOS
	INNER JOIN CONCEPTOS
		ON HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA
		AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
	INNER JOIN PERSONAL
		ON HISTORICOS.COMPANIA = PERSONAL.COMPANIA
		AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
	INNER JOIN DEPENDENCIA
		ON PERSONAL.COMPANIA = DEPENDENCIA.COMPANIA
		AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
	INNER JOIN CENTRO_COSTO
		ON PERSONAL.ANO = CENTRO_COSTO.ANO
		AND PERSONAL.COMPANIA = CENTRO_COSTO.COMPANIA
		AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
	INNER JOIN V_FONDO_DE_PENSIONES
		ON PERSONAL.COMPANIA = V_FONDO_DE_PENSIONES.COMPANIA
		AND PERSONAL.ID_DEL_FONDO = V_FONDO_DE_PENSIONES.ID_DEL_FONDO
WHERE
	HISTORICOS.ID_DE_PROCESO = s$proceso$s
	AND HISTORICOS.ANO = s$ano$s
	AND HISTORICOS.MES = s$mes$s
	AND HISTORICOS.PERIODO = s$periodo$s
GROUP BY 
	HISTORICOS.ID_DE_PROCESO,
	HISTORICOS.ANO,
	HISTORICOS.FECHA,
	HISTORICOS.PERIODO, 
	CENTRO_COSTO.NOMBRE, 
	PERSONAL.ID_CENTRO_DE_COSTO, 
	PERSONAL.ID_DEL_FONDO, 	
	DEPENDENCIA.CODIGO, 
	DEPENDENCIA.NOMBRE,
	V_FONDO_DE_PENSIONES.NOMBRE_DEL_FONDO, 
	V_FONDO_DE_PENSIONES.NIT
ORDER BY 
	PERSONAL.ID_CENTRO_DE_COSTO