SELECT 
    PERSONAL.NUMERO_DCTO CEDULA,
    PERSONAL.NOMBRECOMPLETO NOM_EMPLEADO,
    PERSONAL.CUENTA,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('002') THEN HISTORICOS.VALOR ELSE 0 END) C002,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('097') THEN HISTORICOS.VALOR ELSE 0 END) C097, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('130') THEN HISTORICOS.VALOR ELSE 0 END) C130,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('697') THEN HISTORICOS.VALOR ELSE 0 END) C697,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('698') THEN HISTORICOS.VALOR ELSE 0 END) C698,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('799') THEN HISTORICOS.VALOR ELSE 0 END) C799,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('140') THEN HISTORICOS.VALOR ELSE 0 END) C140,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('144') THEN HISTORICOS.VALOR ELSE 0 END) C144,
    PERSONAL.DEPENDENCIA,
    MAX(DEPENDENCIA.NOMBRE) NOMBREDEPENDENCIA,
    MAX(HISTORICOS.ANO) ANO,
    MAX(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN('002') THEN NVL(CONCEPTOS.CTA_CRE_PPTAL,0) END) CUENTAPPTAL
FROM HISTORICOS
  INNER JOIN CONCEPTOS
    ON HISTORICOS.COMPANIA        = CONCEPTOS.COMPANIA 
    AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
  INNER JOIN PERIODOS
    ON HISTORICOS.COMPANIA        = PERIODOS.COMPANIA
    AND HISTORICOS.ID_DE_PROCESO  = PERIODOS.ID_DE_PROCESO
    AND HISTORICOS.ANO            = PERIODOS.ANO
    AND HISTORICOS.MES            = PERIODOS.MES
    AND HISTORICOS.PERIODO        = PERIODOS.PERIODO 
  INNER JOIN PERSONAL 
    ON HISTORICOS.COMPANIA          = PERSONAL.COMPANIA  
    AND HISTORICOS.ID_DE_EMPLEADO   = PERSONAL.ID_DE_EMPLEADO   
  INNER JOIN DEPENDENCIA
    ON PERSONAL.COMPANIA     = DEPENDENCIA.COMPANIA
    AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
WHERE HISTORICOS.COMPANIA 		=s$compania$s
  AND HISTORICOS.ID_DE_PROCESO 	=s$proceso$s
  AND HISTORICOS.ANO          	=s$ano$s
  AND HISTORICOS.MES          	=s$mes$s
  AND HISTORICOS.PERIODO      	=s$periodo$s
  AND PERIODOS.ACUMULADO NOT IN(0)
GROUP BY  PERSONAL.NUMERO_DCTO ,
          PERSONAL.NOMBRECOMPLETO,
          PERSONAL.CUENTA,
          PERSONAL.DEPENDENCIA
ORDER BY PERSONAL.NUMERO_DCTO,
    PERSONAL.DEPENDENCIA
