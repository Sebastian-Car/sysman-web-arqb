SELECT
PERSONAL.ID_DE_EMPLEADO,
PERSONAL.NOMBRECOMPLETO AS NOMCOMPLETO, 
PERSONAL.NUMERO_DCTO,
PERSONAL.DEPENDENCIA,
DEPENDENCIA.NOMBRE NOMBRE_DEP,
V_CAJA_COMPENSACION.CAJA_COMPENSACION, 
V_CAJA_COMPENSACION.NIT, 
V_CAJA_COMPENSACION.NOMBRE_CAJA, 
HISTORICOS.ID_DE_PROCESO, 
HISTORICOS.ANO, 
HISTORICOS.MES, 
HISTORICOS.PERIODO,
HISTORICOS.ANO,
HISTORICOS.PERIODO,
PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES) NOM_MES, 
MAX(CONCEPTOS.NOMBRE_CONCEPTO) AS NOMBRE_CONCEPTO, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=101 THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=102 THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE_ICBF, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=103 THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE_SENA,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=104 THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE_ESAP, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=105 THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE_INSTITUTOS_TECNICOS, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=107 THEN HISTORICOS.VALOR ELSE 0 END) AS BASE_PARAFISCALSINT, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=108 THEN HISTORICOS.VALOR ELSE 0 END) AS BASE_PARAFISCAL, 
ROUND(SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=108 THEN HISTORICOS.VALOR ELSE 0 END) * 9/100,0) AS TOTAL, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO=101 THEN HISTORICOS.VALOR ELSE 0 END +
CASE WHEN HISTORICOS.ID_DE_CONCEPTO=102 THEN HISTORICOS.VALOR ELSE 0 END +
CASE WHEN HISTORICOS.ID_DE_CONCEPTO=103 THEN HISTORICOS.VALOR ELSE 0 END +
CASE WHEN HISTORICOS.ID_DE_CONCEPTO=104 THEN HISTORICOS.VALOR ELSE 0 END + 
CASE WHEN HISTORICOS.ID_DE_CONCEPTO=105 THEN HISTORICOS.VALOR ELSE 0 END) SUMA,
MAX(HISTORICOS.ID_DE_CONCEPTO) AS ID_DE_CONCEPTO, 
MAX(CASE WHEN PERSONAL.ID_DE_TIPO=02 THEN 'Integral' ELSE (CASE WHEN PERSONAL.REGIMEN=1 THEN 'Retroactivas' ELSE 'Ley 50' END) END) AS REGIMEN, 
CATEGORIA.SALARIO_BASE,
MAX(TO_NUMBER(PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA,47))) AS PAR47,
MAX(TO_NUMBER(PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA,45))) AS PAR45,
MAX(TO_NUMBER(PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA,46))) AS PAR46,
MAX(TO_NUMBER(PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA,46))) AS PAR48,
MAX(TO_NUMBER(PCK_NOMINA_COM1.FC_PARAMETRO(HISTORICOS.COMPANIA,49))) AS PAR49

FROM HISTORICOS 
LEFT JOIN PERSONAL 
ON HISTORICOS.COMPANIA = PERSONAL.COMPANIA 
AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
LEFT JOIN 
V_CAJA_COMPENSACION 
ON PERSONAL.COMPANIA = V_CAJA_COMPENSACION.COMPANIA 
AND PERSONAL.CAJA_COMPENSACION = V_CAJA_COMPENSACION.CAJA_COMPENSACION 
LEFT JOIN CONCEPTOS 
ON HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO 
AND HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA
LEFT JOIN CATEGORIA 
ON PERSONAL.ANO = CATEGORIA.ANO
AND PERSONAL.ID_DE_CATEGORIA = CATEGORIA.ID_DE_CATEGORIA 
AND PERSONAL.ESCALAFON = CATEGORIA.ESCALAFON 
AND PERSONAL.COMPANIA = CATEGORIA.COMPANIA
LEFT JOIN DEPENDENCIA
ON DEPENDENCIA.COMPANIA = HISTORICOS.COMPANIA
AND DEPENDENCIA.CODIGO = PERSONAL.DEPENDENCIA 

WHERE HISTORICOS.COMPANIA = s$compania$s
AND HISTORICOS.ID_DE_PROCESO = s$procesoNomina$s
AND HISTORICOS.ANO = s$anioNomina$s
AND HISTORICOS.MES = s$mesNomina$s
AND HISTORICOS.PERIODO = s$periodoNomina$s
AND HISTORICOS.ID_DE_CONCEPTO IN('101','102','103','104','105','107','108')
GROUP BY PERSONAL.ID_DE_EMPLEADO, 
PERSONAL.NOMBRECOMPLETO,
PERSONAL.DEPENDENCIA,
DEPENDENCIA.NOMBRE,
PERSONAL.NUMERO_DCTO, 
V_CAJA_COMPENSACION.CAJA_COMPENSACION, 
V_CAJA_COMPENSACION.NIT, 
V_CAJA_COMPENSACION.NOMBRE_CAJA, 
HISTORICOS.ID_DE_PROCESO, 
HISTORICOS.ANO, 
HISTORICOS.MES, 
HISTORICOS.PERIODO, 
HISTORICOS.ANO,
HISTORICOS.PERIODO,
CATEGORIA.SALARIO_BASE