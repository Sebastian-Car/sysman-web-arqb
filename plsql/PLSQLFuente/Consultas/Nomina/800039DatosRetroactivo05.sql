SELECT TEMP_SUMA_RETROACTIVO.COMPANIA,
  TEMP_SUMA_RETROACTIVO.ID_DE_PROCESO,
  TO_CHAR(TEMP_SUMA_RETROACTIVO.ANO) ANO,
  TEMP_SUMA_RETROACTIVO.PERIODO,
  TEMP_SUMA_RETROACTIVO.ID_DE_EMPLEADO,
  TEMP_SUMA_RETROACTIVO.ID_DE_CONCEPTO,
  TEMP_SUMA_RETROACTIVO.VALOR,
  PERSONAL.APELLIDO1,
  PERSONAL.APELLIDO2,
  PERSONAL.NOMBRES
FROM TEMP_SUMA_RETROACTIVO
  LEFT JOIN PERSONAL
     ON  TEMP_SUMA_RETROACTIVO.COMPANIA           = PERSONAL.COMPANIA
    AND TEMP_SUMA_RETROACTIVO.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
WHERE TEMP_SUMA_RETROACTIVO.COMPANIA  = s$compania$s 
ORDER BY TEMP_SUMA_RETROACTIVO.COMPANIA,
  TEMP_SUMA_RETROACTIVO.ANO,
  TEMP_SUMA_RETROACTIVO.PERIODO,
  TEMP_SUMA_RETROACTIVO.ID_DE_EMPLEADO,
  TEMP_SUMA_RETROACTIVO.ID_DE_CONCEPTO
