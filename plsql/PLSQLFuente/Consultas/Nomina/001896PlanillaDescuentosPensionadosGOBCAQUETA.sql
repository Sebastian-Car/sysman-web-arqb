SELECT PERSONAL.NUMERO_DCTO AS CEDULA,
    HISTORICOS.ID_DE_EMPLEADO AS CODIGO,
    PERSONAL.ID_DE_CATEGORIA,
    SUM(HISTORICOS.VALOR) AS VALOR,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('799') THEN HISTORICOS.VALOR ELSE 0 END) AS C7999,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO     IN ('601','602','604','607','609','613','618','626','628','640','641','642','643') THEN HISTORICOS.VALOR ELSE 0 END) C6998,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('600','603','605','608','610','611','612','614','615','616','617','619','620','621','622','623','624','625','627','629','630','631','632','633','634','635','636','637','638','639') THEN HISTORICOS.VALOR ELSE 0 END) C6999,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('129','606','681','123') THEN  HISTORICOS.VALOR ELSE 0 END)C8000,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('002') THEN HISTORICOS.VALOR ELSE 0 END) C002,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('097') THEN HISTORICOS.VALOR ELSE 0 END) C097, 
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('130') THEN HISTORICOS.VALOR ELSE 0 END) C130,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('697') THEN HISTORICOS.VALOR ELSE 0 END) C697,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('698') THEN HISTORICOS.VALOR ELSE 0 END) C698,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('799') THEN HISTORICOS.VALOR ELSE 0 END) C799,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('140') THEN HISTORICOS.VALOR ELSE 0 END) C140,
    SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('144') THEN HISTORICOS.VALOR ELSE 0 END) C144,
    MAX(PERSONAL.DEPENDENCIA) AS DEPENDENCIA,
    MAX(DEPENDENCIA.NOMBRE) AS NOMBREDEPENDENCIA,
    MAX(HISTORICOS.ID_DE_PROCESO) AS PROCESO,
    MAX(HISTORICOS.ANO) AS ANO,
    MAX(PCK_SYSMAN_UTL.FC_NOMBRE_MES(TO_CHAR(HISTORICOS.MES))) AS MES,
    MAX(PERSONAL.ID_DE_CARGO) AS CARGO,
    MAX(CARGOS.NOMBRE_DEL_CARGO) AS NOMBRECARGO,
    MAX(PERSONAL.CUENTA) AS CUENTA,
    MAX(HISTORICOS.PERIODO) AS PERIODO,
    MAX(PERSONAL.NOMBRECOMPLETO) AS NOM_EMPLEADO,
    MAX(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('002') THEN NVL(CONCEPTOS.CTA_CRE_PPTAL,0) END) CUENTAPPTAL,  
    SUM(CASE WHEN CONCEPTOS.CLASE IN (3) THEN HISTORICOS.VALOR ELSE 0 END) DEVENGOS,
    SUM(CASE WHEN CONCEPTOS.CLASE IN (5) THEN HISTORICOS.VALOR ELSE 0 END) DESCUENTOS,
    SUM(CASE WHEN CONCEPTOS.CLASE IN (3) THEN HISTORICOS.VALOR ELSE 0 END)-  SUM(CASE WHEN CONCEPTOS.CLASE IN (5) THEN HISTORICOS.VALOR ELSE 0 END) AS NETO
  FROM HISTORICOS
INNER JOIN PERSONAL 
   ON HISTORICOS.COMPANIA       = PERSONAL.COMPANIA
  AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
INNER JOIN DEPENDENCIA 
   ON PERSONAL.COMPANIA    = DEPENDENCIA.COMPANIA
  AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
INNER JOIN CONCEPTOS 
   ON HISTORICOS.COMPANIA       = CONCEPTOS.COMPANIA
  AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
INNER JOIN PERIODOS 
   ON HISTORICOS.COMPANIA      = PERIODOS.COMPANIA
  AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO
  AND HISTORICOS.ANO           = PERIODOS.ANO
  AND HISTORICOS.MES           = PERIODOS.MES
  AND HISTORICOS.PERIODO       = PERIODOS.PERIODO
INNER JOIN CARGOS 
   ON PERSONAL.COMPANIA   = CARGOS.COMPANIA
  AND PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO
WHERE HISTORICOS.COMPANIA = s$compania$s
       AND HISTORICOS.ID_DE_PROCESO= s$proceso$s
       AND HISTORICOS.ANO = s$ano$s
       AND HISTORICOS.MES = s$mes$s
       AND HISTORICOS.PERIODO = s$periodo$s
       AND PERIODOS.ACUMULADO NOT IN 0
GROUP BY PERSONAL.DEPENDENCIA,
         PERSONAL.NUMERO_DCTO,
         HISTORICOS.ID_DE_EMPLEADO,
         PERSONAL.ID_DE_CATEGORIA
ORDER BY PERSONAL.NUMERO_DCTO,
         PERSONAL.DEPENDENCIA
