SELECT
V_ACUMULADOS.ANO,
V_ACUMULADOS.NOMBRECOMPLETO, 
V_ACUMULADOS.CLASE, 
s$CONDIDCONCEPTO$s
MAX(V_ACUMULADOS.ID_DE_EMPLEADO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS ID_DE_EMPLEADO, 
UPPER(V_ACUMULADOS.CLASE_NOMBRE) AS NOMBRECLASE, 
V_ACUMULADOS.NUMERO_DCTO,
V_ACUMULADOS.ID_CENTRO_DE_COSTO AS CC,
V_ACUMULADOS.NOMBRE_CENTRO_DE_COSTO AS NOM_CC,
V_ACUMULADOS.NOMBRE_DE_CARGO AS NOMCARGO,
s$NOMBRECONCEPTOAS$s
MAX(V_ACUMULADOS.SALARIO_BASE_IBC) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS MISUELDOACTUAL, 
MAX(V_ACUMULADOS.NOMBRE_FONDOSALUD) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS NOMBRE_FONDOSALUD, 
MAX(V_ACUMULADOS.NOMBRE_FONDOPENSION) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS NOMBRE_FONDOPENSION,
MAX(V_ACUMULADOS.NOMBRE_FONDOCESANTIAS) KEEP (DENSE_RANK LAST ORDER BY ROWNUM) AS NOMBRE_FONDOCESANTIAS,
SUM(CASE V_ACUMULADOS.MES WHEN 1 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS M01,
SUM(CASE V_ACUMULADOS.MES WHEN 2 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS M02,
SUM(CASE V_ACUMULADOS.MES WHEN 3 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS M03,
SUM(CASE V_ACUMULADOS.MES WHEN 4 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS M04,
SUM(CASE V_ACUMULADOS.MES WHEN 5 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS M05,
SUM(CASE V_ACUMULADOS.MES WHEN 6 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS M06,
SUM(CASE V_ACUMULADOS.MES WHEN 7 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS M07,
SUM(CASE V_ACUMULADOS.MES WHEN 8 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS M08,
SUM(CASE V_ACUMULADOS.MES WHEN 9 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS M09,
SUM(CASE V_ACUMULADOS.MES WHEN 10 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS M10,
SUM(CASE V_ACUMULADOS.MES WHEN 11 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS M11,
SUM(CASE V_ACUMULADOS.MES WHEN 12 THEN V_ACUMULADOS.VALOR ELSE 0 END) AS M12,
SUM(V_ACUMULADOS.VALOR) AS TOTAL,

CASE WHEN ENCARGOS.ID_DE_CARGO  IS NOT NULL 
    AND (SELECT DISTINCT MAX (V_ACUMULADOS.FECHAINICIO) 
		FROM V_ACUMULADOS 
		WHERE V_ACUMULADOS.ANO || LPAD(V_ACUMULADOS.MES,2,'0') || LPAD(V_ACUMULADOS.PERIODO,2,'0') in s$DESDE$s) >= ENCARGOS.FECHAINICIO 
	AND (SELECT DISTINCT MAX (V_ACUMULADOS.FECHA) 
		FROM V_ACUMULADOS 
		WHERE V_ACUMULADOS.ANO || LPAD(V_ACUMULADOS.MES,2,'0') || LPAD(V_ACUMULADOS.PERIODO,2,'0') in s$HASTA$s) <= ENCARGOS.FECHAFINAL
THEN CARGOS.NOMBRE_DEL_CARGO
ELSE NULL
END NOMENCARGO,
CASE WHEN ENCARGOS.ID_DE_CARGO  IS NOT NULL 
	AND (SELECT DISTINCT MAX (V_ACUMULADOS.FECHAINICIO) 
		FROM V_ACUMULADOS 
		WHERE V_ACUMULADOS.ANO || LPAD(V_ACUMULADOS.MES,2,'0') || LPAD(V_ACUMULADOS.PERIODO,2,'0') in s$DESDE$s) >= ENCARGOS.FECHAINICIO 
	AND (SELECT DISTINCT MAX (V_ACUMULADOS.FECHA) 
		FROM V_ACUMULADOS 
		WHERE V_ACUMULADOS.ANO || LPAD(V_ACUMULADOS.MES,2,'0') || LPAD(V_ACUMULADOS.PERIODO,2,'0') in s$HASTA$s) <= ENCARGOS.FECHAFINAL
THEN ENCARGOS.SUELDOMENSUAL
ELSE NULL
END SUELENCARGO
FROM V_ACUMULADOS 
 LEFT JOIN PERSONAL ON
 V_ACUMULADOS.COMPANIA=PERSONAL.COMPANIA
 AND V_ACUMULADOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
 
LEFT JOIN ENCARGOS ON
V_ACUMULADOS.COMPANIA = ENCARGOS.COMPANIA
AND V_ACUMULADOS.ANO = ENCARGOS.ANO
AND V_ACUMULADOS.ID_DE_EMPLEADO = ENCARGOS.ID_DE_EMPLEADO
AND (SELECT DISTINCT MAX (V_ACUMULADOS.FECHAINICIO) 
	FROM V_ACUMULADOS
	WHERE V_ACUMULADOS.ANO || LPAD(V_ACUMULADOS.MES,2,'0') || LPAD(V_ACUMULADOS.PERIODO,2,'0') in s$DESDE$s) >= ENCARGOS.FECHAINICIO 
AND (SELECT DISTINCT MAX (V_ACUMULADOS.FECHA)
	FROM V_ACUMULADOS
	WHERE V_ACUMULADOS.ANO || LPAD(V_ACUMULADOS.MES,2,'0') || LPAD(V_ACUMULADOS.PERIODO,2,'0') in s$HASTA$s) <= ENCARGOS.FECHAFINAL
s$JOINRESUMIDO$s
LEFT JOIN CARGOS ON
V_ACUMULADOS.COMPANIA = CARGOS.COMPANIA
AND ENCARGOS.ID_DE_CARGO = CARGOS.ID_DE_CARGO
WHERE 
V_ACUMULADOS.COMPANIA= s$compania$s
AND V_ACUMULADOS.KARDEX<>0 
AND V_ACUMULADOS.ANO || LPAD(V_ACUMULADOS.MES,2,'0') || LPAD(V_ACUMULADOS.PERIODO,2,'0') 
BETWEEN s$DESDE$s 
AND s$HASTA$s 
s$CONDNULL$s 
s$CONDEMPLEADO$s
 
 
GROUP BY V_ACUMULADOS.NOMBRECOMPLETO,
 V_ACUMULADOS.ID_DE_CONCEPTO,
s$GCONDIDCONCEPTO$s
V_ACUMULADOS.CLASE, 
V_ACUMULADOS.ANO,  
V_ACUMULADOS.CLASE_NOMBRE, 
V_ACUMULADOS.NUMERO_DCTO, 
V_ACUMULADOS.ID_CENTRO_DE_COSTO, 
V_ACUMULADOS.NOMBRE_CENTRO_DE_COSTO, 
V_ACUMULADOS.NOMBRE_DE_CARGO,
ENCARGOS.FECHAINICIO,
PERSONAL.CARGO_ENCARGO,
ENCARGOS.FECHAFINAL,
CARGOS.NOMBRE_DEL_CARGO,
ENCARGOS.SUELDOMENSUAL,
V_ACUMULADOS.NOMBRE_FONDOCESANTIAS,
ENCARGOS.ID_DE_CARGO,
s$NOMBRECONCEPTO$s
 
ORDER BY 
V_ACUMULADOS.NOMBRECOMPLETO, 
V_ACUMULADOS.CLASE, 
s$GCONDIDCONCEPTO$s
V_ACUMULADOS.ANO