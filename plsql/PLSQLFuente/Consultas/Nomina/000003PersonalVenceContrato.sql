SELECT 
   PERSONAL.ID_DE_EMPLEADO COD_EMPLEADO, 
   PERSONAL.NUMERO_DCTO, 
   (PERSONAL.APELLIDO1 || ' ' || PERSONAL.APELLIDO2 || ' ' || PERSONAL.NOMBRES)  NOMBRE_EMPLEADO, 
   (SELECT NOMBRE FROM ESTADO_ACTUAL WHERE CODIGO  = PERSONAL.ESTADO_ACTUAL )  ESTADOACTUAL, 
   PERSONAL.FECHA_DE_INGRESO, 
   PERSONAL.FECHATERCONTRATO, 
   PERSONAL.FECHANCTO, 
   CATEGORIA.SALARIO_BASE 
  FROM 
   PERSONAL 
     INNER JOIN CATEGORIA ON (PERSONAL.COMPANIA = CATEGORIA.COMPANIA) AND (PERSONAL.ESCALAFON = CATEGORIA.ESCALAFON) AND (PERSONAL.ID_DE_CATEGORIA = CATEGORIA.ID_DE_CATEGORIA) AND (PERSONAL.ANO = CATEGORIA.ANO) 
  WHERE 
   PERSONAL.COMPANIA = s$compania$s
  AND PERSONAL.ID_DE_EMPLEADO <>0  
  AND PERSONAL.FECHATERCONTRATO >= s$fechaInicio$s
  AND PERSONAL.FECHATERCONTRATO <= s$fechaFinal$s
  ORDER BY PERSONAL.APELLIDO1, PERSONAL.APELLIDO2, PERSONAL.NOMBRES
