SELECT DISTINCT
        PERSONAL.NOMBRECOMPLETO AS NOMCOMPLETO, 
        PERSONAL.ID_DE_EMPLEADO, 
        PERSONAL.ESTADO_ACTUAL,
        PERSONAL.NUMERO_DCTO, 
        PERSONAL.FECHAFONDOPENSION AS FECHAFONDO, 
        V_FONDO_DE_PENSIONES.ID_DEL_FONDO AS IDFONDO, 
        V_FONDO_DE_PENSIONES.ID_DEL_FONDO,
        V_FONDO_DE_PENSIONES.NIT, 
        V_FONDO_DE_PENSIONES.NOMBRE_DEL_FONDO AS NOMBREFONDO
   FROM PERSONAL 
   LEFT JOIN 
   V_FONDO_DE_PENSIONES ON 
   PERSONAL.ID_DEL_FONDO = V_FONDO_DE_PENSIONES.ID_DEL_FONDO AND 
   PERSONAL.COMPANIA = V_FONDO_DE_PENSIONES.COMPANIA
   WHERE PERSONAL.ID_DE_EMPLEADO NOT IN (0)
   AND PERSONAL.ESTADO_ACTUAL NOT IN (3)
   AND PERSONAL.COMPANIA= s$compania$s
