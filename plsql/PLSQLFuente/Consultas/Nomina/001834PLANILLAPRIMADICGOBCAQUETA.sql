SELECT 
  ESTABLECIMIENTOS_DOCENTES.CODIGO AS ESTABLECIMIENTO,
  ESTABLECIMIENTOS_DOCENTES.NOMBRE AS NOMBREESTABLECIMIENTO,
  PERSONAL.ID_DE_EMPLEADO,
  DEPENDENCIA.CODIGO AS DEPENDENCIA,
  DEPENDENCIA.NOMBRE AS NOMBREDEPENDENCIA,
  PERSONAL.NOMBRECOMPLETO AS NOMCOMPLETO,
  PERSONAL.NUMERO_DCTO,
  PERSONAL.FECHA_DE_INGRESO,
  CASE
    WHEN PERSONAL.FECHA_DE_INGRESO > TO_DATE('01/07/'|| EXTRACT(YEAR FROM SYSDATE) ,'DD/MM/YYYY')
    THEN PERSONAL.FECHA_DE_INGRESO
    ELSE TO_DATE('01/07/'|| EXTRACT(YEAR FROM SYSDATE),'DD/MM/YYYY')
  END FECHA_DE_INGRESOMALLAMAS,
  PERSONAL.FECHATERCONTRATO,
  CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO,
  CARGOS.NOMBRE_DEL_CARGO,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '158' THEN HISTORICOS.VALOR ELSE  0 END)  AS PRIMADIC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '930' THEN HISTORICOS.VALOR ELSE  0 END)  AS SUELDOPRIMADIC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '159' THEN HISTORICOS.VALOR ELSE  0 END) + 
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '160' THEN HISTORICOS.VALOR ELSE  0 END)  AS PRIMAEXTRASEMESTRAL,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '931' THEN HISTORICOS.VALOR ELSE  0 END)  AS PRIMASEMESTRAL,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '932' THEN HISTORICOS.VALOR ELSE  0 END)  AS PRIMAVAC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '933' THEN HISTORICOS.VALOR ELSE  0 END)  AS TRANSPRIMADIC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '934' THEN HISTORICOS.VALOR ELSE  0 END)  AS ALIMENPRIMADIC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '935' THEN HISTORICOS.VALOR ELSE  0 END)  AS GASTOSREP,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '939' THEN HISTORICOS.VALOR ELSE  0 END)  AS BON,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '940' THEN HISTORICOS.VALOR ELSE  0 END)  AS VPA,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '935' THEN HISTORICOS.VALOR ELSE  0 END)  AS VPP,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '067' THEN HISTORICOS.VALOR ELSE  0 END)  AS DIAS067,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '936' THEN HISTORICOS.VALOR ELSE  0 END)  AS DIASPRIMADIC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '937' THEN HISTORICOS.VALOR ELSE  0 END)  AS DIASCOMPRIMADIC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '938' THEN HISTORICOS.VALOR ELSE  0 END)  AS DIASLICPRIMADIC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '942' THEN HISTORICOS.VALOR ELSE  0 END)  AS EXTRAS,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '140' THEN HISTORICOS.VALOR ELSE  0 END)  AS DESCUENTOS,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '144' THEN HISTORICOS.VALOR ELSE  0 END)  AS NETO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '150' THEN HISTORICOS.VALOR ELSE  0 END)  AS BASP,
  CENTRO_COSTO.CODIGO AS ID_CENTRO_DE_COSTO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN '170' THEN HISTORICOS.VALOR ELSE  0 END) AS PANTIG
FROM HISTORICOS
INNER JOIN PERSONAL
  ON HISTORICOS.COMPANIA        = PERSONAL.COMPANIA
  AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
INNER JOIN DEPENDENCIA
  ON PERSONAL.COMPANIA     = DEPENDENCIA.COMPANIA
  AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
INNER JOIN CARGOS
  ON PERSONAL.COMPANIA     = CARGOS.COMPANIA
  AND PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO
LEFT JOIN ESTABLECIMIENTOS_DOCENTES
  ON PERSONAL.COMPANIA                = ESTABLECIMIENTOS_DOCENTES.COMPANIA
  AND PERSONAL.CODIGO_ESTABLECIMIENTO = ESTABLECIMIENTOS_DOCENTES.CODIGO
INNER JOIN CENTRO_COSTO
  ON PERSONAL.COMPANIA            = CENTRO_COSTO.COMPANIA
  AND PERSONAL.ANO                = CENTRO_COSTO.ANO 
  AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
WHERE HISTORICOS.COMPANIA         = s$compania$s
AND HISTORICOS.ID_DE_PROCESO      = 's$proceso$s'
AND HISTORICOS.ANO                = 's$anio$s'
AND HISTORICOS.MES                = 's$mes$s'
AND HISTORICOS.PERIODO            = 's$periodo$s'
AND HISTORICOS.ID_DE_CONCEPTO IN ('158','930','931','932','933','934','935','936','937','938','939','940','942','140','144','159','150','140','097','163','170','160','067')
GROUP BY ESTABLECIMIENTOS_DOCENTES.CODIGO,
  ESTABLECIMIENTOS_DOCENTES.NOMBRE,
  PERSONAL.ID_DE_EMPLEADO,
  DEPENDENCIA.CODIGO,
  PERSONAL.NOMBRECOMPLETO,
  PERSONAL.NUMERO_DCTO,
  PERSONAL.FECHA_DE_INGRESO,
  PERSONAL.FECHATERCONTRATO,
  CENTRO_COSTO.NOMBRE,
  DEPENDENCIA.NOMBRE,
  CARGOS.NOMBRE_DEL_CARGO,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  CENTRO_COSTO.CODIGO
HAVING SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('144') THEN HISTORICOS.VALOR ELSE  0 END) > 0
ORDER BY ESTABLECIMIENTOS_DOCENTES.CODIGO,
  ESTABLECIMIENTOS_DOCENTES.NOMBRE  
