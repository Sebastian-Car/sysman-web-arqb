WITH PRIMATECCORTOLIMA AS
( SELECT 
        NOVEDADES.COMPANIA,
        NOVEDADES.ID_DE_PROCESO,
        NOVEDADES.ANO,
        NOVEDADES.MES,
        NOVEDADES.PERIODO,
        NOVEDADES.ID_DE_EMPLEADO,
        NOVEDADES.ID_DE_CONCEPTO,
        CASE WHEN NOVEDADES.ID_DE_CONCEPTO = 085 THEN NOVEDADES.VALOR ELSE 0 END VPT,
        CASE WHEN NOVEDADES.ID_DE_CONCEPTO = 086 THEN NOVEDADES.VALOR ELSE 0 END VPTNO,
        CASE WHEN NOVEDADES.ID_DE_CONCEPTO = 087 THEN NOVEDADES.VALOR ELSE 0 END VPTNACION
  FROM NOVEDADES
  WHERE NOVEDADES.COMPANIA	=  s$compania$s
    AND NOVEDADES.ANO				=  0 
    AND NOVEDADES.MES				=  0 
    AND NOVEDADES.PERIODO		=  0
    AND NOVEDADES.ID_DE_CONCEPTO 	IN (085,086,087) 
),
FECHAS AS 
( SELECT CODIGO COMPANIA,
         PCK_NOMINA.FC_FECHAINIFINPERIODO(s$compania$s,s$procesoNomina$s,s$anioNomina$s,s$mesNomina$s,s$periodoNomina$s,1) UNO,
         PCK_NOMINA.FC_FECHAINIFINPERIODO(s$compania$s,s$procesoNomina$s,s$anioNomina$s,s$mesNomina$s,s$periodoNomina$s,2) DOS
  FROM COMPANIA
  WHERE CODIGO  = s$compania$s
)
SELECT DISTINCT
      PERSONAL.ESTADO_ACTUAL,
      CATEGORIA.CODIGOGRADO  CODIGO,
      PERSONAL.ID_DE_CARGO  CODIGOCG,
      PERSONAL.ESCALAFON,
      PERSONAL.ID_DE_EMPLEADO  CODIGOEMPLEADO, 
      CENTRO_COSTO.CODIGO ID_CENTRO_COSTO,
      CENTRO_COSTO.NOMBRE NOMBRECC,
      PERSONAL.NOMBRECOMPLETO,
      PERSONAL.NUMERO_DCTO DOC,
      CATEGORIA.SALARIO_BASE,
      PERSONAL.FECHANCTO FENCTO,
      PERSONAL.FECHA_DE_INGRESO FEIN,
      CATEGORIA.ANO,
      NVL(PRIMATECCORTOLIMA.VPT,0) VPT,
      NVL(PRIMATECCORTOLIMA.VPTNO,0) VPTNO,
      NVL(PRIMATECCORTOLIMA.VPTNACION,0) VPTNACION,
      ROUND(CATEGORIA.SALARIO_BASE*(NVL(PRIMATECCORTOLIMA.VPT,0)+ NVL(PRIMATECCORTOLIMA.VPTNO,0)+NVL(PRIMATECCORTOLIMA.VPTNACION,0))/100) AS VALOR,
      (CATEGORIA.SALARIO_BASE)+(ROUND(CATEGORIA.SALARIO_BASE*(NVL(PRIMATECCORTOLIMA.VPT,0)+NVL(PRIMATECCORTOLIMA.VPTNO,0)+NVL(PRIMATECCORTOLIMA.VPTNACION,0))/100)) AS VALORTOTAL,
      PERSONAL.FECHA_DE_RETIRO FECHARET,
      PERSONAL.FECHA_DE_INGRESO FECHAING,
      PERSONAL.COMPANIA    
FROM PERSONAL INNER JOIN FECHAS
  ON PERSONAL.COMPANIA        = FECHAS.COMPANIA
LEFT JOIN PRIMATECCORTOLIMA
 ON PERSONAL.COMPANIA 			  =  PRIMATECCORTOLIMA.COMPANIA
AND PERSONAL.ID_DE_EMPLEADO 	=  PRIMATECCORTOLIMA.ID_DE_EMPLEADO
LEFT JOIN CATEGORIA 
   ON PERSONAL.COMPANIA 			  = CATEGORIA.COMPANIA 
   AND PERSONAL.ESCALAFON 			= CATEGORIA.ESCALAFON 
   AND PERSONAL.ID_DE_CATEGORIA = CATEGORIA.ID_DE_CATEGORIA 
LEFT JOIN CENTRO_COSTO 
 ON PERSONAL.COMPANIA 			    = CENTRO_COSTO.COMPANIA 
AND	PERSONAL.ANO                = CENTRO_COSTO.ANO
AND	PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
WHERE PERSONAL.COMPANIA			    =   s$compania$s
  AND PERSONAL.ID_DE_EMPLEADO   NOT IN(0)
  AND CATEGORIA.ANO 				    =   s$anioNomina$s
  AND (
      (PERSONAL.ESTADO_ACTUAL      <>  3 
   AND PERSONAL.FECHA_DE_INGRESO    <=  FECHAS.DOS
      )
   OR 
      (PERSONAL.ESTADO_ACTUAL 			=  3 
   AND PERSONAL.FECHA_DE_RETIRO     >= FECHAS.UNO 
   AND PERSONAL.FECHA_DE_INGRESO    <= FECHAS.DOS
      )
      )
ORDER BY PERSONAL.ESTADO_ACTUAL,
         CATEGORIA.CODIGOGRADO,
         PERSONAL.NOMBRECOMPLETO
