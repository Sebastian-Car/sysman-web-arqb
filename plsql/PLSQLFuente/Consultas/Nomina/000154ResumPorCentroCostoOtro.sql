MERGE INTO CONSULTAS FIN USING (SELECT '000154ResumPorCentroCostoOtro' INFORME ,TO_CLOB(q'[SELECT DISTINCT
    V_ACUMULADOS.ID_CENTRO_DE_COSTO CENTRO_COSTO, 
    V_ACUMULADOS.NOMBRE_CENTRO_DE_COSTO, 
    V_ACUMULADOS.CLASE, 
    SUM(V_ACUMULADOS.VALOR) SUMA_DE_VALOR, 
    CASE WHEN V_ACUMULADOS.CLASE = 3
    THEN 'VALOR DEVENGOS'
    WHEN V_ACUMULADOS.CLASE = 5
    THEN 'VALOR DESCUENTOS'
    ELSE 'CONCEPTOS CONTABLES'
    END CLASECONCEPTO
FROM V_ACUMULADOS
WHERE (LPAD(ID_DE_PROCESO,2,'0')
    || LPAD(V_ACUMULADOS.ANO,4,'0') 
    || LPAD(V_ACUMULADOS.MES,2,'0') 
    || LPAD(V_ACUMULADOS.PERIODO, 2,'0'))
    BETWEEN  s$codigoInicial$s AND s$codigoFinal$s
    AND V_ACUMULADOS.COMPANIA = 's$compania$s' 
    s$filtro$s
GROUP BY 
    V_ACUMULADOS.ID_CENTRO_DE_COSTO, 
    V_ACUMULADOS.NOMBRE_CENTRO_DE_COSTO, 
    V_ACUMULADOS.CLASE,
    CASE WHEN  V_ACUMULADOS.CLASE = 3 
    THEN 'VALOR DEVENGOS' 
    WHEN V_ACUMULADOS.CLASE = 5 
    THEN 'VALOR DESCUENTOS' 
    ELSE 'CONCEPTOS CONTABLES'
    END, 
    V_ACUMULADOS.ID_CENTRO_DE_COSTO
HAVING V_ACUMULADOS.CLASE = 8 
    OR V_ACUMULADOS.CLASE = 3 
    OR V_ACUMULADOS.CLASE = 5 ]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);