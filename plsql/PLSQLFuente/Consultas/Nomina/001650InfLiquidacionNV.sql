SELECT SOLICITUD.COMPANIA,
  PERSONAL.APELLIDO1 || ' '|| PERSONAL.APELLIDO2 || ' '|| PERSONAL.NOMBRES FUNAREAMISL,
  CARGOS.NOMBRE_DEL_CARGO CARGOAREMIS,
  SOLICITUD.OBJETO,
  CIUDAD.NOMBRE DESTINO,
  SOLICITUD.FECHAINICIO,
  SOLICITUD.FECHAFIN,
  CASE Solicitud.TVISITA
    WHEN '00'
    THEN 'Pernoctando'
    WHEN '01'
    THEN 'Sin Pernoctar'
    WHEN '02'
    THEN 'Sabana'
  END TIPOVISITA,
  CASE SOLICITUD.VEHICULO
    WHEN -1
    THEN 'X'
    ELSE ' '
  END VEH,
  SOLICITUD.JUSTIFICACION,
  SOLICITUD.CODSOLICITUD,
  SOLICITUD.NUMSOLICITUD,
  --VI_TIPOLUGAR.NOMBRE,
  CASE SOLICITUD.TRSNESPECIAL
    WHEN -1
    THEN 'X'
    ELSE ' '
  END TRSNESPECIAL,
  VI_DETALLE_VIATICOS.NUMDIASPER,
  VI_DETALLE_VIATICOS.NUMDIASSINPER,
  SOLICITUD.NODIAS,
  VI_DETALLE_VIATICOS.NUMDIASSINPER +
  VI_DETALLE_VIATICOS.NUMDIASPER      DIAS,
  SOLICITUD.VALORTOTALVIATICO,
  SUM(
  CASE CC.TIPO_CONCEPTO
    WHEN 'T'
    THEN VI_DETALLE_VIATICOS.TOTAL
    ELSE 0
  END) TRANSPORTE,
  SUM(
  CASE CC.TIPO_CONCEPTO
    WHEN 'H'
    THEN VI_DETALLE_VIATICOS.TOTAL
    ELSE 0
  END) VIATICOS
FROM VI_VIATICOS SOLICITUD
  LEFT JOIN CIUDAD
    ON SOLICITUD.PAIS          = CIUDAD.PAIS
    AND SOLICITUD.DEPARTAMENTO = CIUDAD.DEPARTAMENTO
    AND SOLICITUD.CIUDAD       = CIUDAD.CODIGO
  LEFT JOIN PERSONAL
    ON SOLICITUD.COMPANIA    = PERSONAL.COMPANIA
   AND SOLICITUD.TERCERO = PERSONAL.NUMERO_DCTO
   AND SOLICITUD.SUCURSAL = PERSONAL.SUCURSAL
   LEFT JOIN CARGOS
   ON CARGOS.COMPANIA= PERSONAL.COMPANIA
   AND CARGOS.ID_DE_CARGO= PERSONAL.ID_DE_CARGO
  LEFT JOIN VI_DETALLE_VIATICOS
    ON SOLICITUD.COMPANIA      = VI_DETALLE_VIATICOS.COMPANIA
    AND SOLICITUD.ANO          = VI_DETALLE_VIATICOS.ANO
    AND SOLICITUD.TIPO_VIATICO = VI_DETALLE_VIATICOS.TIPO_VIATICO
    AND SOLICITUD.CODSOLICITUD = VI_DETALLE_VIATICOS.NUMERO
  LEFT JOIN VI_CONCEPTO_VIATICOS CC
    ON VI_DETALLE_VIATICOS.COMPANIA  = CC.COMPANIA
    AND VI_DETALLE_VIATICOS.ANO      = CC.ANO
    AND VI_DETALLE_VIATICOS.CONCEPTO = CC.CODIGO_CONCEPTO
WHERE SOLICITUD.COMPANIA         = s$compania$s
  AND SOLICITUD.CODSOLICITUD     = s$solicitud$s
GROUP BY SOLICITUD.COMPANIA,
  PERSONAL.APELLIDO1
  || ' '
  || PERSONAL.APELLIDO2
  || ' '
  || PERSONAL.NOMBRES ,
    CARGOS.NOMBRE_DEL_CARGO,
  SOLICITUD.OBJETO,
  CIUDAD.NOMBRE,
  SOLICITUD.FECHAINICIO,
  SOLICITUD.FECHAFIN,
  CASE Solicitud.TVISITA
    WHEN '00'
    THEN 'Pernoctando'
    WHEN '01'
    THEN 'Sin Pernoctar'
    WHEN '02'
    THEN 'Sabana'
  END,
  CASE SOLICITUD.VEHICULO
    WHEN -1
    THEN 'X'
    ELSE ' '
  END,
  SOLICITUD.JUSTIFICACION,
  SOLICITUD.OBSERVACIONCOMITE,
  CASE SOLICITUD.APROBADO
    WHEN -1
    THEN 'X'
    ELSE ' '
  END,
  SOLICITUD.CODSOLICITUD,
  SOLICITUD.NUMSOLICITUD,
  CASE SOLICITUD.TRSNESPECIAL
    WHEN -1
    THEN 'X'
    ELSE ' '
  END,
  VI_DETALLE_VIATICOS.NUMDIASPER,
  VI_DETALLE_VIATICOS.NUMDIASSINPER,
  SOLICITUD.NODIAS,
  VI_DETALLE_VIATICOS.NUMDIASSINPER+VI_DETALLE_VIATICOS.NUMDIASPER,
  SOLICITUD.VALORTOTALVIATICO
