SELECT 
  ERRORESINICIALES.ERROR,
  ERRORESINICIALES.DESCRIPCION,
  ERRORESINICIALES.ANOE,
  ERRORESINICIALES.MESE,
  ERRORESINICIALES.PERIODOE,
  COMPANIA.NOMBRE COMPANIA
FROM ERRORESINICIALES
INNER JOIN COMPANIA
ON ERRORESINICIALES.COMPANIA = COMPANIA.CODIGO
WHERE ERRORESINICIALES.COMPANIA = s$compania$s
