SELECT HISTORICOS.ID_DE_CONCEPTO,
  SUM(HISTORICOS.VALOR) AS SUMADEVALOR,
  CONCEPTOS.NOMBRE_CONCEPTO,
  CONCEPTOS.CN_UGPP,
  HISTORICOS.ANO
FROM HISTORICOS
LEFT JOIN CONCEPTOS
ON HISTORICOS.COMPANIA        = CONCEPTOS.COMPANIA
AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
WHERE HISTORICOS.COMPANIA = s$compania$s
AND CONCEPTOS.CN_UGPP       IS NULL
AND HISTORICOS.ANO           = s$ano$s
AND CONCEPTOS.CLASE          =3
GROUP BY HISTORICOS.ID_DE_CONCEPTO,
  CONCEPTOS.NOMBRE_CONCEPTO,
  CONCEPTOS.CN_UGPP,
  HISTORICOS.ANO,
  CONCEPTOS.CLASE
HAVING SUM(HISTORICOS.VALOR)>0
