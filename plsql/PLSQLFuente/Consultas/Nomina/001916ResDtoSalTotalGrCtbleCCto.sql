SELECT  
  PERSONAL_HISTORICO.NOMBRE_FONDOSALUD,  
  V_FONDO_DE_SALUD.NIT NITFONDO,  
  PERSONAL_HISTORICO.ID_CENTRO_DE_COSTO,  
  PERSONAL_HISTORICO.NOMBRE_CENTRO_DE_COSTO,  
  CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE='A' 
    THEN 'ADMINISTRACIÓN' 
    ELSE  
       CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE ='V'  
       THEN 'VENTAS ' 
       ELSE 
        CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE='P' 
        THEN 'PRODUCCIÓN' 
        ELSE 'OTROS' END 
      END 
    END GRUPOCONTABLE, 
  SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO IN (134) THEN V_ACUMULADOS.VALOR ELSE 0 END)  IBL,  
  SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO IN (112) THEN V_ACUMULADOS.VALOR ELSE 0 END)   IBC,  
  SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO IN (113) THEN V_ACUMULADOS.VALOR ELSE 0 END)   SALUDEMPLEADO,  
  SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO IN (116) THEN V_ACUMULADOS.VALOR ELSE 0 END)   SALUDPATRONO,  
  SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO IN (114) THEN V_ACUMULADOS.VALOR ELSE 0 END)   GARANTIAEMPLEADO,  
  SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO IN (119) THEN V_ACUMULADOS.VALOR ELSE 0 END)   GARANTIAPATRONO, 
  SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO IN (123) THEN V_ACUMULADOS.VALOR ELSE 0 END)   UPC,  
  SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO IN (369)  
      THEN  
        CASE WHEN V_ACUMULADOS.VALOR<0 THEN V_ACUMULADOS.VALOR*-1 ELSE V_ACUMULADOS.VALOR END  
      ELSE 0 END + 
      CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO IN (399) 
      THEN  
        CASE WHEN V_ACUMULADOS.VALOR <0 THEN V_ACUMULADOS.VALOR*-1 ELSE V_ACUMULADOS.VALOR END  
      ELSE 0  END + 
      CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO IN (398) 
      THEN  
        CASE WHEN V_ACUMULADOS.VALOR <0 THEN V_ACUMULADOS.VALOR*-1 ELSE V_ACUMULADOS.VALOR END 
      ELSE 0 END)  DESCUENTOS 
FROM V_ACUMULADOS  
  INNER JOIN PERSONAL_HISTORICO  
    ON  V_ACUMULADOS.COMPANIA = PERSONAL_HISTORICO.COMPANIA 
   AND  V_ACUMULADOS.ID_DE_PROCESO = PERSONAL_HISTORICO.ID_DE_PROCESO 
   AND  V_ACUMULADOS.ANO = PERSONAL_HISTORICO.ANO 
   AND  V_ACUMULADOS.MES = PERSONAL_HISTORICO.MES 
   AND  V_ACUMULADOS.PERIODO = PERSONAL_HISTORICO.PERIODO 
   AND  V_ACUMULADOS.ID_DE_EMPLEADO = PERSONAL_HISTORICO.ID_DE_EMPLEADO 
  INNER JOIN V_FONDO_DE_SALUD 
     ON PERSONAL_HISTORICO.COMPANIA = V_FONDO_DE_SALUD.COMPANIA 
    AND PERSONAL_HISTORICO.FONDO_SALUD = V_FONDO_DE_SALUD.FONDO_SALUD     
WHERE V_ACUMULADOS.COMPANIA = s$compania$s  
  AND V_ACUMULADOS.ID_DE_PROCESO = s$idProceso$s 
  AND V_ACUMULADOS.ANO = s$ano$s  
  AND V_ACUMULADOS.MES = s$mes$s  
GROUP BY PERSONAL_HISTORICO.NOMBRE_FONDOSALUD,  
         V_FONDO_DE_SALUD.NIT, 
         PERSONAL_HISTORICO.ID_CENTRO_DE_COSTO,  
         PERSONAL_HISTORICO.NOMBRE_CENTRO_DE_COSTO,  
         CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE='A' 
    THEN 'ADMINISTRACIÓN' 
    ELSE  
       CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE ='V'  
       THEN 'VENTAS ' 
       ELSE 
        CASE WHEN PERSONAL_HISTORICO.GRUPOCONTABLE='P' 
        THEN 'PRODUCCIÓN' 
        ELSE 'OTROS' END 
      END 
    END
