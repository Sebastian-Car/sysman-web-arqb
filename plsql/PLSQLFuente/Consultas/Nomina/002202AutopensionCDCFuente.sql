SELECT DISTINCT
PERSONAL.ID_DE_EMPLEADO,
PERSONAL.NOMBRECOMPLETO,
PERSONAL.NUMERO_DCTO AS CEDULA,
PERSONAL.FECHA_DE_INGRESO, 
HISTORICOS.ANO,
HISTORICOS.MES,
HISTORICOS.PERIODO,
PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES)  NOMBRE_MES,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '112' THEN HISTORICOS.VALOR ELSE 0 END) AS IBC,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '118' THEN HISTORICOS.VALOR ELSE 0 END) AS PEMP,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '117' THEN HISTORICOS.VALOR ELSE 0 END) AS PPAT, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '124' THEN HISTORICOS.VALOR ELSE 0 END) AS PVOL,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '118' THEN HISTORICOS.VALOR ELSE 0 END)  +
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '117' THEN HISTORICOS.VALOR ELSE 0 END) +
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '124' THEN HISTORICOS.VALOR ELSE 0 END) AS PEMPPPATPVOL,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '115' THEN HISTORICOS.VALOR ELSE 0 END) AS FSP, 
ESTABLECIMIENTOS_DOCENTES.CODIGO AS ID_FUENTE,
ESTABLECIMIENTOS_DOCENTES.NOMBRE AS FUENTE, 
V_FONDO_DE_PENSIONES.ID_DEL_FONDO,
V_FONDO_DE_PENSIONES.NOMBRE_DEL_FONDO
FROM 
HISTORICOS
LEFT JOIN PERSONAL
ON PERSONAL.COMPANIA = HISTORICOS.COMPANIA
AND PERSONAL.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO

LEFT JOIN ESTABLECIMIENTOS_DOCENTES 
ON PERSONAL.COMPANIA = ESTABLECIMIENTOS_DOCENTES.COMPANIA 
AND PERSONAL.CODIGO_ESTABLECIMIENTO = ESTABLECIMIENTOS_DOCENTES.CODIGO

LEFT JOIN PERIODOS 
ON HISTORICOS.COMPANIA = PERIODOS.COMPANIA
AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO 
AND HISTORICOS.ANO = PERIODOS.ANO 
AND HISTORICOS.MES = PERIODOS.MES 
AND HISTORICOS.PERIODO = PERIODOS.PERIODO
LEFT JOIN V_FONDO_DE_PENSIONES 
ON PERSONAL.COMPANIA = V_FONDO_DE_PENSIONES.COMPANIA
AND PERSONAL.ID_DEL_FONDO = V_FONDO_DE_PENSIONES.ID_DEL_FONDO
WHERE 
HISTORICOS.ID_DE_CONCEPTO IN ('112', '118', '117', '124', '115')
AND HISTORICOS.ID_DE_PROCESO = s$procesoNomina$s
AND HISTORICOS.COMPANIA = s$compania$s
AND PERIODOS.ACUMULADO NOT IN (0)
AND PERSONAL.ID_DE_EMPLEADO <>'00000'
AND HISTORICOS.ANO = s$anioNomina$s
AND HISTORICOS.MES = s$mesNomina$s
AND HISTORICOS.PERIODO = s$periodoNomina$s
GROUP BY
PERSONAL.ID_DE_EMPLEADO, PERSONAL.NOMBRECOMPLETO, PERSONAL.NUMERO_DCTO, PERSONAL.FECHA_DE_INGRESO, HISTORICOS.ANO, 
HISTORICOS.MES, HISTORICOS.PERIODO, ESTABLECIMIENTOS_DOCENTES.CODIGO, ESTABLECIMIENTOS_DOCENTES.NOMBRE, V_FONDO_DE_PENSIONES.ID_DEL_FONDO, 
V_FONDO_DE_PENSIONES.NOMBRE_DEL_FONDO