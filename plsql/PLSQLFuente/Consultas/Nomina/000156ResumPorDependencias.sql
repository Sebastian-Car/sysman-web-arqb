SELECT DISTINCT V_ACUMULADOS.CLASE,
  SUM(V_ACUMULADOS.VALOR) SUMA_DE_VALOR,
  CASE
    WHEN V_ACUMULADOS.CLASE = 3
    THEN 'VALOR DEVENGOS'
    WHEN V_ACUMULADOS.CLASE = 5
    THEN 'VALOR DESCUENTOS'
    ELSE 'CONCEPTOS CONTABLES'
  END CLASECONCEPTO,
  V_ACUMULADOS.NOMBRE_DEPENDENCIA NOMBRE,
  V_ACUMULADOS.DEPENDENCIA
FROM V_ACUMULADOS
WHERE V_ACUMULADOS.COMPANIA = 's$compania$s' 
AND   (LPAD(ID_DE_PROCESO,2,'0')
  || LPAD(V_ACUMULADOS.ANO,4,'0')
  || LPAD(V_ACUMULADOS.MES,2,'0')
  || LPAD(V_ACUMULADOS.PERIODO, 2,'0')) BETWEEN s$codigoInicial$s AND s$codigoFinal$s
s$filtro$s
GROUP BY V_ACUMULADOS.CLASE,
  CASE
    WHEN V_ACUMULADOS.CLASE = 3
    THEN 'VALOR DEVENGOS'
    WHEN V_ACUMULADOS.CLASE = 5
    THEN 'VALOR DESCUENTOS'
    ELSE 'CONCEPTOS CONTABLES'
  END,
  V_ACUMULADOS.NOMBRE_DEPENDENCIA,
  V_ACUMULADOS.DEPENDENCIA
HAVING V_ACUMULADOS.CLASE = 8
OR V_ACUMULADOS.CLASE     = 3
OR V_ACUMULADOS.CLASE     = 5
