SELECT PERSONAL.ID_DE_EMPLEADO,
  MIN(V_FONDO_DE_PENSION_AFC.FONDO_AFC) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) FONDO_PENSION_VOL,
  MIN(V_FONDO_DE_PENSION_AFC.NIT) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) NIT,
  MIN(V_FONDO_DE_PENSION_AFC.NOMBRE_FONDO_AFC) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) NOMBRE_DEL_FONDO,
  MIN(PERSONAL.NOMBRECOMPLETO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) NOMCOMPLETO,
  PERSONAL.NUMERO_DCTO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO 	IN (112) THEN HISTORICOS.VALOR ELSE 0 END) IBC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO 	IN (127) THEN HISTORICOS.VALOR ELSE 0 END) AVP,
  PCK_SYSMAN_UTL.FC_ROUND(
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO 	IN (302) THEN HISTORICOS.VALOR ELSE 0 END)   /
  (SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO 	IN (124) THEN HISTORICOS.VALOR ELSE 0 END    +
      (CASE WHEN HISTORICOS.ID_DE_CONCEPTO 	IN (127) THEN HISTORICOS.VALOR ELSE 0 END))) *
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO 	IN (127) THEN HISTORICOS.VALOR ELSE 0 END),0) BEN_TRB,
  MIN(PERSONAL.CUENTA_VIVIENDA2) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) CUENTA_VIVIENDA2
FROM HISTORICOS
  INNER JOIN PERSONAL
    ON HISTORICOS.COMPANIA      	= PERSONAL.COMPANIA 
    AND HISTORICOS.ID_DE_EMPLEADO 	= PERSONAL.ID_DE_EMPLEADO
  LEFT JOIN V_FONDO_DE_PENSION_AFC
    ON PERSONAL.COMPANIA              = V_FONDO_DE_PENSION_AFC.COMPANIA
    AND PERSONAL.FONDO_AFC            = V_FONDO_DE_PENSION_AFC.FONDO_AFC
WHERE HISTORICOS.COMPANIA       = s$compania$s
  AND HISTORICOS.ID_DE_PROCESO  = s$procesoNomina$s
  AND HISTORICOS.ANO            = s$anioNomina$s
  AND HISTORICOS.MES            = s$mesNomina$s
  AND HISTORICOS.ID_DE_EMPLEADO NOT IN(0)
  AND HISTORICOS.ID_DE_CONCEPTO IN(112,124,127,302)
GROUP BY  PERSONAL.ID_DE_EMPLEADO,
          PERSONAL.NUMERO_DCTO
HAVING SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN (127) THEN HISTORICOS.VALOR ELSE 0 END) > 0
