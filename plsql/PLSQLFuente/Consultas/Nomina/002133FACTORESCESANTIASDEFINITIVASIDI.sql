SELECT PERSONAL.ID_DE_EMPLEADO,
  PERSONAL.NOMBRECOMPLETO,
  PERSONAL.FONDO_CESANTIAS,
  PERSONAL.NUMERO_DCTO,
  TO_CHAR(PERSONAL.FECHA_DE_INGRESO,'DD/MM/YYYY')AS FECHA_DE_INGRESO,
  CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO,
  DEPENDENCIA.NOMBRE AS DEPENDENCIA,
  CARGOS.NOMBRE_DEL_CARGO,
  HISTORICOS.ID_DE_PROCESO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '177' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO ='178' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO ='191' THEN HISTORICOS.VALOR ELSE 0 END) AS CES,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '182' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO ='169' THEN HISTORICOS.VALOR ELSE 0 END) AS INTCES,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '901' THEN HISTORICOS.VALOR ELSE 0 END) AS SUELDOCES,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '906' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMAANTIGUEDAD,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '908' THEN HISTORICOS.VALOR ELSE 0 END) AS GASTOS,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '903' THEN HISTORICOS.VALOR ELSE 0 END) AS TRANSCES,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '904' THEN HISTORICOS.VALOR ELSE 0 END) AS ALIMENCES,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '992' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMANAVCES,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '988' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMAJUNCES,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '849' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMAVACCES,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '913' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMATEC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '914' THEN HISTORICOS.VALOR ELSE 0 END) AS BONIF,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '899' THEN HISTORICOS.VALOR ELSE 0 END) AS SUELDOBASE,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '905' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMALOCCES,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '909' THEN HISTORICOS.VALOR ELSE 0 END) AS QUINQUENIOCES,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '990' THEN HISTORICOS.VALOR ELSE 0 END) AS DIASCES,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '177' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO ='178' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO ='191' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '484' THEN HISTORICOS.VALOR ELSE 0 END) AS BASE,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '484' THEN HISTORICOS.VALOR ELSE 0 END) AS ANTICIPOSCES,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '484' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '177' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO ='178' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO ='191' THEN HISTORICOS.VALOR ELSE 0 END) AS SOBTOTAL,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '957' THEN HISTORICOS.VALOR ELSE 0 END) AS DIASLICCES,
  ROUND(SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '988' THEN HISTORICOS.VALOR ELSE 0 END) /12,0)  AS DOCPJUN,
  ROUND(SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '849' THEN HISTORICOS.VALOR ELSE 0 END)/12,0)   AS DOCPVAC,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '909' THEN HISTORICOS.VALOR ELSE 0 END) /12 AS DOCQUIN,
  ROUND(SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '992' THEN HISTORICOS.VALOR ELSE 0 END) /12,0)  AS DOCPNAV,
  ROUND(SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '914' THEN HISTORICOS.VALOR ELSE 0 END) /12,0)  AS DOCBON,  
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '913' THEN HISTORICOS.VALOR ELSE 0 END) AS PROMEDIOCES,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '957' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '990' THEN HISTORICOS.VALOR ELSE 0 END)AS TOTALDIAS,
  MAX (TO_CHAR (HISTORICOS.FECHA,'DD/MM/YYYY'))      AS FECHALIQ,
  PERSONAL.ID_CENTRO_DE_COSTO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '485' THEN HISTORICOS.VALOR ELSE 0 END) AS TOTALPARCIALES,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '177' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '178' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN  HISTORICOS.ID_DE_CONCEPTO = '197' THEN HISTORICOS.VALOR ELSE 0 END) AS VRCESANTIAS,
  TO_CHAR(PERSONAL.FECHA_DE_RETIRO,'DD/MM/YYYY')AS FECHA_DE_RETIRO,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '191' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '177' THEN HISTORICOS.VALOR  ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '178' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '197' THEN HISTORICOS.VALOR ELSE 0 END) AS CESP,
  V_FONDO_DE_CESANTIAS.NOMBRE_FONDO_CESANTIAS,
  CARGOS.NOMBRE_DEL_CARGO,
  CARGOS.CODIGOCARGO,
  CARGOS.GRADO
FROM HISTORICOS
INNER JOIN PERSONAL
ON HISTORICOS.COMPANIA        = PERSONAL.COMPANIA
AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
LEFT JOIN CENTRO_COSTO
ON PERSONAL.COMPANIA   = CENTRO_COSTO.COMPANIA
AND PERSONAL.ANO       = CENTRO_COSTO.ANO
AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
LEFT JOIN DEPENDENCIA
ON PERSONAL.COMPANIA     = DEPENDENCIA.COMPANIA
AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
LEFT JOIN CARGOS
ON PERSONAL.COMPANIA   = CARGOS.COMPANIA
AND PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO
LEFT JOIN V_FONDO_DE_CESANTIAS
ON PERSONAL.COMPANIA = V_FONDO_DE_CESANTIAS.COMPANIA
AND PERSONAL.FONDO_CESANTIAS        = V_FONDO_DE_CESANTIAS.FONDO_CESANTIAS
WHERE HISTORICOS.COMPANIA = s$compania$s
  AND HISTORICOS.PERIODO  = s$periodo$s
  AND HISTORICOS.ANO      = s$anio$s
  AND HISTORICOS.MES      = s$mes$s
  AND HISTORICOS.ID_DE_CONCEPTO IN ('191','197','198','178','182','182','169','177','484','485','624','900','901','992','903','904',
 '905','906','907','908','909','910','911','912','913','914','988','989','991','957','189','849','992','990')
  --AND PERSONAL.FONDO_CESANTIAS > ='1'
  AND PERSONAL.ESTADO_ACTUAL = 3
GROUP BY PERSONAL.ID_DE_EMPLEADO,
  PERSONAL.NOMBRECOMPLETO,
  PERSONAL.FONDO_CESANTIAS,
  PERSONAL.NUMERO_DCTO,
  PERSONAL.FECHA_DE_INGRESO,
  CENTRO_COSTO.NOMBRE,
  DEPENDENCIA.NOMBRE,
  HISTORICOS.ID_DE_PROCESO,
  PERSONAL.ID_CENTRO_DE_COSTO,
  PERSONAL.FECHA_DE_RETIRO,
  V_FONDO_DE_CESANTIAS.NOMBRE_FONDO_CESANTIAS,
  CARGOS.NOMBRE_DEL_CARGO,
  CARGOS.CODIGOCARGO,
  CARGOS.GRADO,
  CARGOS.NOMBRE_DEL_CARGO
  HAVING 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '191' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '177'THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '178' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '197'THEN HISTORICOS.VALOR ELSE 0 END)<>0
  ORDER BY  PERSONAL.NOMBRECOMPLETO