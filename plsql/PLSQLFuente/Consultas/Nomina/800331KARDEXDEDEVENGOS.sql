FROM (
SELECT SUM(HISTORICOS.VALOR) OVER (PARTITION BY HISTORICOS.ID_DE_CONCEPTO) TOTAL,
HISTORICOS.ID_DE_CONCEPTO,
PERSONAL.APELLIDO1 || ' ' || 
 PERSONAL.APELLIDO2 || ' ' || 
 PERSONAL.NOMBRES AS NOMBRECOMPLETO,
