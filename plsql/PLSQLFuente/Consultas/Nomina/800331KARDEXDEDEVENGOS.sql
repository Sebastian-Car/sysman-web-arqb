 ON HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO 
AND HISTORICOS.COMPANIA       = PERSONAL.COMPANIA
WHERE HISTORICOS.COMPANIA = s$compania$s 
AND (TO_NUMBER(HISTORICOS.ANO) * 100 + TO_NUMBER(HISTORICOS.MES)) 
    BETWEEN (TO_NUMBER(s$anioInicial$s) * 100 + TO_NUMBER(s$mesInicial$s)) 
        AND (TO_NUMBER(s$anioFinal$s) * 100 + TO_NUMBER(s$mesFinal$s))
AND CONCEPTOS.CLASE IN (3) 
AND PERIODOS.ACUMULADO NOT IN (0)
GROUP BY HISTORICOS.VALOR,
PERSONAL.APELLIDO1 || ' ' || 
 PERSONAL.APELLIDO2 || ' ' || 
 PERSONAL.NOMBRES ,
