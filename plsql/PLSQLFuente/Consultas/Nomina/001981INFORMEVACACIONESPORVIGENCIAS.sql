MERGE INTO CONSULTAS FIN USING (SELECT '001981INFORMEVACACIONESPORVIGENCIAS' INFORME ,TO_CLOB(q'[SELECT 
    APELLIDO1 || ' ' || APELLIDO2 || ' ' || NOMBRES  NOMBRECOMPLETO,
    PERSONAL.NUMERO_DCTO, 
    HISTORICOS.ID_DE_EMPLEADO,
    SUM(CASE 
        WHEN HISTORICOS.ID_DE_CONCEPTO = 151
        THEN HISTORICOS.VALOR ELSE 0
        END  )BONIRECREACION,
    SUM(CASE 
        WHEN HISTORICOS.ID_DE_CONCEPTO = 155 
        THEN HISTORICOS.VALOR ELSE 0
        END ) PRIMAVACACIONES,  
    SUM(CASE 
        WHEN HISTORICOS.ID_DE_CONCEPTO = 174
        THEN HISTORICOS.VALOR ELSE 0
        END  )SALARIODEVACACIONES,
    SUM(CASE 
        WHEN HISTORICOS.ID_DE_CONCEPTO = 175 
        THEN HISTORICOS.VALOR ELSE 0
        END  )INDEMNIZACIONVACACIONES,
    MIN(VACACIONES.FECHA_INICIO)  KEEP (DENSE_RANK LAST ORDER BY ROWNUM)  PRIMERODEFECHA_INICIO,
    MIN(VACACIONES.FECHA_FINAL)  KEEP (DENSE_RANK LAST ORDER BY ROWNUM)  PRIMERODEFECHA_FINAL,
    MIN(VACACIONES.INICIO_DISFRUTE)  KEEP (DENSE_RANK LAST ORDER BY ROWNUM)  PRIMERODEINICIO_DISFRUTE,
    MIN(VACACIONES.FINAL_DISFRUTE)  KEEP (DENSE_RANK LAST ORDER BY ROWNUM)  PRIMERODEFINAL_DISFRUTE,
    CASE WHEN TO_CHAR(VACACIONES.FECHA_FINAL, 'YYYY') = s$anio$s THEN 'ACTUAL' ELSE 'ANTERIOR' END VIGENCIA,
    PERSONAL.ID_CENTRO_DE_COSTO,
    CENTRO_COSTO.NOMBRE
FROM HISTORICOS 
LEFT JOIN VACACIONES 
    ON HISTORICOS.COMPANIA = VACACIONES.COMPANIA
    AND HISTORICOS.ID_DE_PROCESO = VACACIONES.ID_DE_PROCESO
    AND HISTORICOS.ANO = VACACIONES.ANO
    AND HISTORICOS.MES = VACACIONES.MES
    AND HISTORICOS.PERIODO = VACACIONES.PERIODO
    AND HISTORICOS.ID_DE_EMPLEADO = VACACIONES.ID_DE_EMPLEADO
    AND HISTORICOS.FECHA = VACACIONES.FECHAPAGO
INNER JOIN PERSONAL 
     ON HISTORICOS.COMPANIA = PERSONAL.COMPANIA
    AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO  
INNER JOIN CENTRO_COSTO 
     ON PERSONAL.COMPANIA = CENTRO_COSTO.COMPANIA
    AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
    AND HISTORICOS.ANO = CENTRO_COSTO.ANO 
WHERE 
    HISTORICOS.COMPANIA = s$compania$s
    AND HISTORICOS.ID_DE_CONCEPTO IN(151,155,174,175)
    AND HISTORICOS.ANO = s$anio$s
    AND HISTORICOS.MES = s$mes$s 
    AND HISTORICOS.PERIODO= s$periodo$s
GROUP BY 
    APELLIDO1 || ' ' || APELLIDO2 || ' ' || NOMBRES  ,
    PERSONAL.NUMERO_DCTO,
    HISTORICOS.COMPANIA,
    HISTORICOS.ID_DE_PROCESO,
    HISTORICOS.ANO,
    HISTORICOS.MES,
    HISTORICOS.PERIODO,
    HISTORICOS.ID_DE_EMPLEADO,
    VACACIONES.FECHA_INICIO,
    VACACIONES.FECHA_FINAL,
    VACACIONES.INICIO_DISFRUTE,
    VACACIONES.FINAL_DISFRUTE,
    CASE WHEN TO_CHAR(VACACIONES.FECHA_FINAL, 'YYYY') = s$anio$s THEN 'ACTUAL' ELSE 'ANTERIOR' END,
    PERSONAL.ID_CENTRO_DE_COSTO,
    CENTRO_COSTO.NOMBRE
]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);