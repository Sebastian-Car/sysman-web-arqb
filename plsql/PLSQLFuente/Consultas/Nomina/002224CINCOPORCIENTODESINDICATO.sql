MERGE INTO CONSULTAS FIN USING (SELECT '002224CINCOPORCIENTODESINDICATO' INFORME ,TO_CLOB(q'[SELECT 	SUMADEVALOR,
		SINTRAGOBERNAR,
		TOTALSINTRAESTATALES,
		ROUND(TOTALSINTRAESTATALES * 0.05,0) AS "5S",
		ROUND(TOTALSINTRAESTATALES * 0.95,0) AS "95S",
		S195 AS "95S1",
		CINCOPORCIENTO,
		SALDO
FROM
(
SELECT  SUM(HISTORICOS.VALOR) AS SUMADEVALOR, 
		SUM(
			DECODE(HISTORICOS.ID_DE_CONCEPTO,PCK_SYSMAN_UTL.FC_PAR(HISTORICOS.COMPANIA,'CODIGO CONCEPTO SINDICATO SINTRAESTATALES',6, SYSDATE),HISTORICOS.VALOR,0)
			) SINTRAGOBERNAR,
		SUM(
			DECODE(HISTORICOS.ID_DE_CONCEPTO,'129',HISTORICOS.VALOR,'645',HISTORICOS.VALOR,'629',HISTORICOS.VALOR,0)
			) AS TOTALSINTRAESTATALES,
		
		SUM	(
			CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '129' THEN HISTORICOS.VALOR ELSE 0 END + 
			CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '129' THEN HISTORICOS.VALOR ELSE 0 END
			) AS "S195",
		ROUND(SUM(VALOR*5/100),0)AS CINCOPORCIENTO,
		ROUND(SUM(VALOR)) - ROUND(SUM(VALOR*5/100),0) AS SALDO
FROM 	HISTORICOS LEFT JOIN CONCEPTOS
		ON HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
		AND HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA
WHERE  	HISTORICOS.COMPANIA = s$compania$s
		AND HISTORICOS.ANO = s$ano1$s 
		AND HISTORICOS.MES = s$mes1$s
		AND HISTORICOS.ID_DE_CONCEPTO IN ('129','645','629','679')
)]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, 'ECABRERA' MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
COMMIT;