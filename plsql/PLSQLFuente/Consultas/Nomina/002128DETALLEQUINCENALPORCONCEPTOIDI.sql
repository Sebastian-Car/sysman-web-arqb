SELECT HISTORICOS.ID_DE_CONCEPTO,
CONCEPTOS.NOMBRE_CONCEPTO, 
PERSONAL.ID_DE_EMPLEADO,
PERSONAL.NOMBRECOMPLETO AS NOCOMPLETO, 
HISTORICOS.VALOR AS SVALOR, 
PERIODOS.FECHAFINAL,
PERSONAL.NUMERO_DCTO,
PERSONAL.ID_DE_TIPO, 
TIPOS_DE_EMPLEADO.NOMBRE_TIPO, 
HISTORICOS.ID_DE_PROCESO, 
PERSONAL.ID_CENTRO_DE_COSTO
FROM HISTORICOS 
    LEFT JOIN PERSONAL 
        ON HISTORICOS.COMPANIA = PERSONAL.COMPANIA
        AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
    LEFT JOIN PERIODOS 
        ON HISTORICOS.COMPANIA = PERIODOS.COMPANIA
        AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO
        AND HISTORICOS.ANO = PERIODOS.ANO
        AND HISTORICOS.MES = PERIODOS.MES
        AND HISTORICOS.PERIODO = PERIODOS.PERIODO 
    LEFT JOIN CONCEPTOS 
        ON HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA
        AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
    LEFT JOIN TIPOS_DE_EMPLEADO 
        ON PERSONAL.COMPANIA = TIPOS_DE_EMPLEADO.COMPANIA
        AND PERSONAL.ID_DE_TIPO = TIPOS_DE_EMPLEADO.ID_DE_TIPO
WHERE HISTORICOS.COMPANIA=s$compania$s 
AND HISTORICOS.ID_DE_PROCESO=s$proceso$s
AND HISTORICOS.ANO=s$anio$s
AND HISTORICOS.MES=s$mes$s
AND HISTORICOS.PERIODO=s$periodo$s
AND HISTORICOS.ID_DE_CONCEPTO=s$concepto$s
AND PERSONAL.ID_DE_TIPO = CASE WHEN 's$filtro$s' = 0 THEN 's$tipoEmpleado$s' ELSE PERSONAL.ID_DE_TIPO END
ORDER BY HISTORICOS.ID_DE_CONCEPTO, PERSONAL.ID_DE_EMPLEADO