SELECT DISTINCT TO_CHAR(VI.FECHA, 'DD/MM/YYYY') FECHA,
  VI.CIUDAD_ORIGEN,
  VI.TERCERO,
  T.NOMBRE,
  CG.NOMBRE_DEL_CARGO,
  D.NOMBRE DEPENDENCIA,
  CIO.NOMBRE CIUDAD_LABORA,
  CID.NOMBRE CIUDAD_DESTINO,
  EXTRACT(DAY FROM VI.FECHAINICIO) DIA_INICIO,
  EXTRACT(MONTH FROM VI.FECHAINICIO) MES_INICIO,
  EXTRACT(YEAR FROM VI.FECHAINICIO) ANIO_INICIO,
  EXTRACT(DAY FROM VI.FECHAFIN) DIA_FIN,
  EXTRACT(MONTH FROM VI.FECHAFIN) MES_FIN,
  EXTRACT(YEAR FROM VI.FECHAFIN) ANIO_FIN,
  CASE
    WHEN VI.SOLICITA_TIQUETE NOT IN (0)
    THEN 'X'
    ELSE ''
  END SI_TIQUETE,
  CASE
    WHEN VI.SOLICITA_TIQUETE IN (0)
    THEN 'X'
    ELSE ''
  END NO_TIQUETE,
  VI.OBJETO,
  VI.OBSERVACION,
  VI.VALOR_TRANSPORTE,
  CASE WHEN TO_CHAR(VI.DESTINO_CORRERIA) IS NULL OR TO_CHAR(VI.DESTINO_CORRERIA) = 'EMPTY_CLOB()'
        THEN ' '
        ELSE TO_CHAR(VI.DESTINO_CORRERIA)
  END DESTINO_CORRERIA
FROM VI_VIATICOS VI
INNER JOIN TERCERO T
ON VI.COMPANIA  = T.COMPANIA
AND VI.TERCERO  = T.NIT
AND VI.SUCURSAL = T.SUCURSAL
LEFT JOIN PERSONAL P
ON T.COMPANIA        = P.COMPANIA
AND T.NIT            = P.NUMERO_DCTO
AND T.SUCURSAL       = P.SUCURSAL
AND P.ESTADO_ACTUAL IN (1)
INNER JOIN DEPENDENCIA D
ON VI.COMPANIA     = D.COMPANIA
AND VI.DEPENDENCIA = D.CODIGO
LEFT JOIN CARGOS CG
ON P.COMPANIA     = CG.COMPANIA
AND P.ID_DE_CARGO = CG.ID_DE_CARGO
INNER JOIN CIUDAD CIO
ON VI.CIUDAD_ORIGEN        = CIO.CODIGO
AND VI.DEPARTAMENTO_ORIGEN = CIO.DEPARTAMENTO
AND VI.PAIS_ORIGEN         = CIO.PAIS
INNER JOIN CIUDAD CID
ON VI.CIUDAD_DESTINO        = CID.CODIGO
AND VI.DEPARTAMENTO_DESTINO = CID.DEPARTAMENTO
AND VI.PAIS_DESTINO         = CID.PAIS
WHERE VI.COMPANIA           = s$compania$s
AND VI.CODSOLICITUD         = s$solicitud$s
