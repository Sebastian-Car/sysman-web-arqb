SELECT TRIM(PERSONAL.APELLIDO1) || ' ' || TRIM(PERSONAL.APELLIDO2) || ' ' || TRIM(PERSONAL.NOMBRES) AS NOMBRECOMPLETO,
CARGOS.NOMBRE_DEL_CARGO, 
PERSONAL.NUMERO_DCTO, 
PERSONAL.COMPANIA,
TO_CHAR(PERSONAL.FECHA_DE_INGRESO,'DD/MM/YYYY') AS FECHA_DE_INGRESO,  
MAX(TO_CHAR(HISTORICOS.FECHA,'DD/MM/YYYY')) AS FECHA, 
DEPENDENCIA.NOMBRE, 
TO_CHAR(PERSONAL.FECHA_INICIO,'DD/Mon/YYYY') AS FECHA_INICIO, 
TO_CHAR(PERSONAL.FECHA_FINAL,'DD/Mon/YYYY') AS FECHA_FINAL, 
TO_CHAR(PERSONAL.INICIO_DISFRUTE,'DD/Mon/YYYY') AS INICIO_DISFRUTE, 
TO_CHAR(PERSONAL.FINAL_DISFRUTE,'DD/Mon/YYYY') AS FINAL_DISFRUTE, 
HISTORICOS.ANO,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='802' THEN HISTORICOS.VALOR ELSE 0 END) AS FACTOR1,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='802' THEN HISTORICOS.VALOR ELSE 0 END) AS FACTOR2, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='803' THEN HISTORICOS.VALOR ELSE 0 END) AS FACTOR3, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='804' THEN HISTORICOS.VALOR ELSE 0 END) AS FACTOR4, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='805' THEN HISTORICOS.VALOR ELSE 0 END) AS FACTOR5, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='806' THEN HISTORICOS.VALOR ELSE 0 END) AS FACTOR6, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='807' THEN HISTORICOS.VALOR ELSE 0 END) AS FACTOR7, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='808' THEN HISTORICOS.VALOR ELSE 0 END) AS FACTOR8, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='809' THEN HISTORICOS.VALOR ELSE 0 END) AS FACTOR9, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='810' THEN HISTORICOS.VALOR ELSE 0 END) AS FACTOR10, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='811' THEN HISTORICOS.VALOR ELSE 0 END) AS FACTOR11, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='812' THEN HISTORICOS.VALOR ELSE 0 END) AS FACTOR12, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='813' THEN HISTORICOS.VALOR ELSE 0 END) AS FACTOR13, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='164' THEN HISTORICOS.VALOR ELSE 0 END) AS NUMPER, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='094' THEN HISTORICOS.VALOR ELSE 0 END) AS DIASCAL, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='162' THEN HISTORICOS.VALOR ELSE 0 END) AS DIASHAB, 
TRUNC(SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='814' THEN HISTORICOS.VALOR ELSE 0 END)) AS ANTIGUEDAD, 
(AVG(ROUND(PCK_SYSMAN_UTL.FC_DIASMESCOMERCIAL(PERSONAL.FECHA_DE_INGRESO,PERSONAL.INICIO_DISFRUTE)/360,0)))-1 AS ANTIGUEDAD1,
PCK_SYSMAN_UTL.FC_DIASHABIL(PERSONAL.COMPANIA,PERSONAL.INICIO_DISFRUTE,PERSONAL.FINAL_DISFRUTE,NULL)AS DIASHABILESDISFRUTE,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='174' THEN HISTORICOS.VALOR ELSE 0 END) AS SALVAC, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='175' THEN HISTORICOS.VALOR ELSE 0 END) AS VACDINERO, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='155' THEN HISTORICOS.VALOR ELSE 0 END) AS PRIMVAC, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='802' THEN HISTORICOS.VALOR ELSE 0 END)
+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='803' THEN HISTORICOS.VALOR ELSE 0 END)
+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='804' THEN HISTORICOS.VALOR ELSE 0 END)
+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='805' THEN HISTORICOS.VALOR ELSE 0 END)
+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='806' THEN HISTORICOS.VALOR ELSE 0 END)
+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='807' THEN HISTORICOS.VALOR ELSE 0 END)
+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='808' THEN HISTORICOS.VALOR ELSE 0 END)
+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='809' THEN HISTORICOS.VALOR ELSE 0 END)
+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='810' THEN HISTORICOS.VALOR ELSE 0 END)
+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='811' THEN HISTORICOS.VALOR ELSE 0 END)
+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='812' THEN HISTORICOS.VALOR ELSE 0 END)
+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='813' THEN HISTORICOS.VALOR ELSE 0 END) AS TOTALFACTOR, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='174' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='175' THEN HISTORICOS.VALOR ELSE 0 END)
+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='155' THEN HISTORICOS.VALOR ELSE 0 END)+SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='873' THEN HISTORICOS.VALOR ELSE 0 END) AS TOTALPAGAR, 
PERSONAL.ID_DE_EMPLEADO, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='873' THEN HISTORICOS.VALOR ELSE 0 END) AS RECREACION, 
 CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO,
CARGOS.ID_DE_CARGO,
CARGOSE.NOMBRE_DEL_CARGO AS NOMBRE_DEL_CARGO_ESCALERA, 
CARGOST.NOMBRE_DEL_CARGO AS NOMBRE_DEL_CARGOTEMPORAL,
PERSONAL.ID_DE_CARGO_TEMPORAL, 
PERSONAL.ID_DE_CARGOESCALERA
FROM HISTORICOS 
INNER JOIN CONCEPTOS 
ON CONCEPTOS.ID_DE_CONCEPTO = HISTORICOS.ID_DE_CONCEPTO
INNER JOIN PERSONAL 
ON PERSONAL.COMPANIA=HISTORICOS.COMPANIA
AND PERSONAL.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
INNER JOIN DEPENDENCIA
ON DEPENDENCIA.CODIGO = PERSONAL.DEPENDENCIA
AND DEPENDENCIA.COMPANIA=PERSONAL.COMPANIA
INNER JOIN CENTRO_COSTO 
ON CENTRO_COSTO.CODIGO= PERSONAL.ID_CENTRO_DE_COSTO 
AND CENTRO_COSTO.COMPANIA=PERSONAL.COMPANIA
AND CENTRO_COSTO.ANO=HISTORICOS.ANO
LEFT JOIN CARGOS 
ON CARGOS.ID_DE_CARGO = PERSONAL.ID_DE_CARGO 
AND CARGOS.COMPANIA=PERSONAL.COMPANIA
LEFT JOIN CARGOS CARGOST
ON CARGOST.ID_DE_CARGO = PERSONAL.ID_DE_CARGO_TEMPORAL 
AND CARGOST.COMPANIA=PERSONAL.COMPANIA
LEFT JOIN CARGOS CARGOSE
ON CARGOSE.ID_DE_CARGO = PERSONAL.ID_DE_CARGOESCALERA 
AND CARGOSE.COMPANIA=PERSONAL.COMPANIA
WHERE 
(HISTORICOS.ID_DE_CONCEPTO>'800' 
AND HISTORICOS.ID_DE_CONCEPTO<'874' 
OR HISTORICOS.ID_DE_CONCEPTO='094' 
OR HISTORICOS.ID_DE_CONCEPTO='162' 
OR HISTORICOS.ID_DE_CONCEPTO='164' 
OR HISTORICOS.ID_DE_CONCEPTO='155' 
OR HISTORICOS.ID_DE_CONCEPTO='174' 
OR HISTORICOS.ID_DE_CONCEPTO='175')
AND HISTORICOS.ANO=s$anioNomina$s
AND HISTORICOS.MES=s$mesNomina$s
AND HISTORICOS.PERIODO=s$periodoNomina$s
AND HISTORICOS.ID_DE_PROCESO=s$procesoNomina$s
GROUP BY TRIM(PERSONAL.APELLIDO1) || ' ' || TRIM(PERSONAL.APELLIDO2) || ' ' || TRIM(PERSONAL.NOMBRES),
CARGOS.NOMBRE_DEL_CARGO, 
PERSONAL.NUMERO_DCTO, 
PERSONAL.FECHA_DE_INGRESO, 
DEPENDENCIA.NOMBRE, 
PERSONAL.FECHA_INICIO, 
PERSONAL.FECHA_FINAL, 
PERSONAL.INICIO_DISFRUTE, 
PERSONAL.FINAL_DISFRUTE, 
PERSONAL.ID_DE_EMPLEADO, 
CENTRO_COSTO.NOMBRE, 
CARGOS.ID_DE_CARGO,
CARGOS.NOMBRE_DEL_CARGO,
CARGOST.NOMBRE_DEL_CARGO,
CARGOSE.NOMBRE_DEL_CARGO,
PERSONAL.ID_DE_CARGO_TEMPORAL, 
PERSONAL.ID_DE_CARGOESCALERA,
HISTORICOS.FECHA,
HISTORICOS.ANO
,
PERSONAL.COMPANIA
HAVING SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO='155' THEN HISTORICOS.VALOR ELSE 0 END)<>0
ORDER BY NOMBRECOMPLETO,
CARGOS.NOMBRE_DEL_CARGO