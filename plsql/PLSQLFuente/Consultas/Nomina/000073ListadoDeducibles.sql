SELECT PERSONAL.NOMBRECOMPLETO  NOMBRECOMPLETO, 
       CONCEPTOS.NOMBRE_CONCEPTO, 
       NOVEDADES.ID_DE_EMPLEADO, 
       NOVEDADES.VALOR, 
       NOVEDADES.ID_DE_PROCESO, 
       NOVEDADES.ANO, 
       NOVEDADES.MES, 
       NOVEDADES.PERIODO, 
       NOVEDADES.ID_DE_CONCEPTO, 
       CASE WHEN NOVEDADES.ID_DE_CONCEPTO = 300 THEN NOVEDADES.VALOR ELSE 0 END  DEDUCIBLE, 
       PERSONAL.PROCESORETENCION, 
       PERSONAL.FECHA_DE_INGRESO 
FROM NOVEDADES 
INNER JOIN PERSONAL 
  ON  NOVEDADES.COMPANIA          = PERSONAL.COMPANIA 
  AND NOVEDADES.ID_DE_EMPLEADO    = PERSONAL.ID_DE_EMPLEADO 
INNER JOIN CONCEPTOS 
  ON  NOVEDADES.COMPANIA          = CONCEPTOS.COMPANIA 
  AND NOVEDADES.ID_DE_CONCEPTO    = CONCEPTOS.ID_DE_CONCEPTO 
WHERE NOVEDADES.COMPANIA= s$compania$s
  AND NOVEDADES.ID_DE_PROCESO    IN  (s$procesoNomina$s,0) 
  AND NOVEDADES.ANO              =   0 
  AND NOVEDADES.MES              =   0 
  AND NOVEDADES.PERIODO          =   0 
  AND NOVEDADES.ID_DE_CONCEPTO   IN  (PCK_NOMINA_COM1.FC_PARAMETRO(s$compania$s,12),301,303,304 )     
  AND NOVEDADES.VALOR            >   0
ORDER BY PERSONAL.NOMBRECOMPLETO, 
         NOVEDADES.ID_DE_EMPLEADO, 
         NOVEDADES.ID_DE_PROCESO, 
         NOVEDADES.ANO, 
         NOVEDADES.MES, 
         NOVEDADES.PERIODO, 
         NOVEDADES.ID_DE_CONCEPTO
