WITH 
PERS AS
(
SELECT CENTRO_COSTO.COMPANIA, 
    CENTRO_COSTO.CODIGO, 
    CENTRO_COSTO.ANO, 
    PERSONAL.ID_DE_EMPLEADO,
    PERSONAL.NOMBRECOMPLETO, 
    PERSONAL.NUMERO_DCTO, 
    PERSONAL.FECHA_DE_INGRESO, 
    PERSONAL.FECHAINIPROGVAC, 
    PERSONAL.FECHAFINPROGVAC, 
    TO_DATE('s$fechaRef$s'  , 'DD/MM/YYYY') FECHREF,
    CENTRO_COSTO.NOMBRE, 
    CARGOS.NOMBRE_DEL_CARGO || ' ' || CATEGORIA.CODIGOGRADO AS NOMBRE_DEL_CARGO, 
    (CASE WHEN PERSONAL.FECHA_FINAL IS NULL THEN PERSONAL.FECHA_DE_INGRESO ELSE PERSONAL.FECHA_FINAL+1 END) FIN,
    PCK_NOMINA.FC_AUSENTISMOEMPLEADO(s$compania$s,PERSONAL.ID_DE_EMPLEADO,( CASE WHEN PERSONAL.FECHA_FINAL IS NULL THEN PERSONAL.FECHA_DE_INGRESO ELSE PERSONAL.FECHA_FINAL+1 END ),SYSDATE) AS AUSENTISMO
FROM PERSONAL INNER JOIN CENTRO_COSTO ON  PERSONAL.COMPANIA = CENTRO_COSTO.COMPANIA  AND  PERSONAL.ANO = CENTRO_COSTO.ANO  AND  PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO    
    INNER JOIN cargos ON  PERSONAL.COMPANIA = cargos.COMPANIA  AND  PERSONAL.ID_DE_CARGO = cargos.ID_DE_CARGO   
    INNER JOIN CATEGORIA ON  PERSONAL.ANO = CATEGORIA.ANO  AND  PERSONAL.ID_DE_CATEGORIA = CATEGORIA.ID_DE_CATEGORIA  AND  PERSONAL.ESCALAFON = CATEGORIA.ESCALAFON  AND  PERSONAL.COMPANIA = CATEGORIA.COMPANIA 
WHERE PERSONAL.COMPANIA= s$compania$s
    AND PERSONAL.ID_DE_EMPLEADO NOT IN(0)
    AND PERSONAL.ESTADO_ACTUAL NOT IN (3,2)	
)
SELECT PERS.COMPANIA, 
    PERS.CODIGO,
    PERS.ID_DE_EMPLEADO,
    PERS.NOMBRECOMPLETO NomCompleto, 
    PERS.NUMERO_DCTO, 
    TO_CHAR(PERS.FECHA_DE_INGRESO, 'DD/MM/YYYY') FECHA_DE_INGRESO, 
    TO_CHAR(PERS.FECHAINIPROGVAC, 'DD/MM/YYYY') FECHAINIPROGVAC, 
    TO_CHAR(PERS.FECHAFINPROGVAC, 'DD/MM/YYYY') FECHAFINPROGVAC, 
    PERS.NOMBRE, 
    PERS.NOMBRE_DEL_CARGO,
    PERS.FIN,
    PERS.AUSENTISMO,
    TO_CHAR(MAX(VACACIONES.FECHA_INICIO) KEEP (DENSE_RANK LAST ORDER BY ROWNUM), 'DD/MM/YYYY') AS UltimoDeFecha_Inicio, 
    TO_CHAR(MAX(VACACIONES.FECHA_FINAL) KEEP (DENSE_RANK LAST ORDER BY ROWNUM), 'DD/MM/YYYY') AS UltimoDeFECHA_FINAL, 
    TO_CHAR(MAX(VACACIONES.INICIO_DISFRUTE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM), 'DD/MM/YYYY') AS UltimoDeInicio_disfrute,
    TO_CHAR(MAX(VACACIONES.FINAL_DISFRUTE) KEEP (DENSE_RANK LAST ORDER BY ROWNUM), 'DD/MM/YYYY') AS UltimoDeFinal_Disfrute,
    TO_CHAR(PERS.FIN, 'DD/MM/YYYY') AS FIVP, 
    TO_CHAR(PERS.FECHREF-PERS.AUSENTISMO, 'DD/MM/YYYY') AS FFVP, 
    PERS.FECHREF AS fechaperiodo,
    PCK_SYSMAN_UTL.FC_ROUND((PCK_SYSMAN_UTL.FC_DIASMESCOMERCIAL(PERS.FIN,PERS.FECHREF-PERS.AUSENTISMO)*15/360),2) AS PERIODOS
FROM PERS LEFT JOIN VACACIONES ON PERS.COMPANIA = VACACIONES.COMPANIA AND PERS.ID_DE_EMPLEADO = VACACIONES.ID_DE_EMPLEADO 
GROUP BY PERS.COMPANIA, 
    PERS.CODIGO, 
    PERS.ANO,
    PERS.ID_DE_EMPLEADO,
    PERS.NOMBRECOMPLETO, 
    PERS.NUMERO_DCTO, 
    PERS.FECHA_DE_INGRESO, 
    PERS.FECHAINIPROGVAC, 
    PERS.FECHAFINPROGVAC, 
    PERS.NOMBRE, 
    PERS.NOMBRE_DEL_CARGO,
    PERS.FIN,
    PERS.AUSENTISMO,
    PERS.FECHREF
ORDER BY PERS.COMPANIA, 
    PERS.CODIGO, 
    PERS.ANO, 
    PERS.NOMBRECOMPLETO
