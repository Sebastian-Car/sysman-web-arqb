SELECT DISTINCT
    V_ACUMULADOS.ID_DE_CONCEPTO, 
    V_ACUMULADOS.NOMBRE_CONCEPTO, 
    V_ACUMULADOS.CLASE, 
    SUM(V_ACUMULADOS.VALOR)  SUMA_DE_VALOR, 
    CONCEPTOS.RELACIONADO, 
    V_ACUMULADOS.CLASE_NOMBRE_MAYU CLASECONCEPTO 
FROM V_ACUMULADOS 
INNER JOIN CONCEPTOS 
ON V_ACUMULADOS.COMPANIA = CONCEPTOS.COMPANIA
AND V_ACUMULADOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO  
WHERE V_ACUMULADOS.COMPANIA = s$compania$s 
AND V_ACUMULADOS.CLASE IN (3, 4, 5, 6, 7, 8)
AND LPAD(ID_DE_PROCESO, 2, 0) || LPAD(ANO, 4, 0) || LPAD(MES, 2, 0) || LPAD(PERIODO, 2, 0) BETWEEN 's$fechaInicial$s'  AND 's$fechaFinal$s'    
AND V_ACUMULADOS.DEPENDENCIA <> '05' 
AND V_ACUMULADOS.GRUPOCONTABLE = 'P'
GROUP BY 
    V_ACUMULADOS.ID_DE_CONCEPTO, 
    V_ACUMULADOS.NOMBRE_CONCEPTO, 
    V_ACUMULADOS.CLASE, 
    CONCEPTOS.RELACIONADO, 
    V_ACUMULADOS.CLASE_NOMBRE_MAYU
ORDER BY 
    V_ACUMULADOS.ID_DE_CONCEPTO
