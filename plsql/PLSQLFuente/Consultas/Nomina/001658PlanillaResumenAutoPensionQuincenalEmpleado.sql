SELECT 
  NOMBREEMPRESA,
  ID_DE_EMPLEADO,
  NOMBRE_DEL_FONDO,
  ID_DEL_FONDO,
  CEDULA,
  NOM_EMPLEADO,
  FECHA,
  NVL(C134,0) C134,
  NVL(C120,0) C120,
  NVL(C131,0) C131,
  NVL(C132,0) C132,
  CODAIFP
 FROM (
  SELECT DISTINCT
    COMPANIA.NOMBRE NOMBREEMPRESA,
    V_PERSONAL.ID_DE_EMPLEADO,
    V_PERSONAL.NOMBRE_FONDOPENSION  NOMBRE_DEL_FONDO,
    V_PERSONAL.ID_DEL_FONDO,
    V_PERSONAL.NUMERO_DCTO   CEDULA,
    V_PERSONAL.APELLIDO1 ||' '||V_PERSONAL.APELLIDO2||' '||V_PERSONAL.NOMBRES  AS NOM_EMPLEADO,
    HISTORICOS.PERIODO ||' '|| PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES) || ' de ' || HISTORICOS.ANO AS FECHA,
    HISTORICOS.ID_DE_CONCEPTO,
    V_FONDO_DE_PENSIONES.CODAIFP,
    HISTORICOS.VALOR
FROM HISTORICOS
	INNER JOIN V_PERSONAL
		ON HISTORICOS.COMPANIA        = V_PERSONAL.COMPANIA
		AND HISTORICOS.ID_DE_EMPLEADO = V_PERSONAL.ID_DE_EMPLEADO
	INNER JOIN V_FONDO_DE_PENSIONES
		ON V_PERSONAL.COMPANIA      = V_FONDO_DE_PENSIONES.COMPANIA
		AND V_PERSONAL.ID_DEL_FONDO = V_FONDO_DE_PENSIONES.ID_DEL_FONDO
  INNER JOIN COMPANIA 
 		 ON HISTORICOS.COMPANIA = COMPANIA.CODIGO
WHERE HISTORICOS.COMPANIA           = s$compania$s
  AND HISTORICOS.ID_DE_PROCESO      =  s$procesoNomina$s
  AND HISTORICOS.ANO                = s$anioNomina$s
  AND HISTORICOS.MES                = s$mesNomina$s
  AND HISTORICOS.PERIODO            =  s$periodoNomina$s
  AND HISTORICOS.ID_DE_CONCEPTO IN ('131','132','134','120'))
PIVOT (SUM(NVL(VALOR,0)) FOR ID_DE_CONCEPTO IN ('131' C131,'132' C132,'134' C134,'120' C120))
ORDER BY CEDULA
