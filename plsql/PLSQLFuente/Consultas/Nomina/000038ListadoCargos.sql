SELECT DISTINCT 
    ID_DE_CARGO,
    ESCALAFON.CODIGO CODIGO_ESCALAFON, 
    ESCALAFON.NOMBRE NOMBREESCALAFON, 
    NOMBRE_DEL_CARGO, 
    CARRERA, 
    GRADO, 
    PLAZAS, 
    FUNCIONES, 
    ENCARRERA 
FROM CARGOS INNER JOIN ESCALAFON 
  ON CARGOS.COMPANIA  = ESCALAFON.COMPANIA 
 AND CARGOS.ESCALAFON = ESCALAFON.CODIGO 
WHERE CARGOS.COMPANIA = s$compania$s
ORDER BY CODIGO_ESCALAFON
