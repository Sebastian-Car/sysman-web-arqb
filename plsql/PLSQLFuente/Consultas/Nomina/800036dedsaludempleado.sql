WITH ACUMULADO_NOVEDAD_130 AS (SELECT V_ACUMULADOS.COMPANIA,
                                      V_ACUMULADOS.ANO, 
                                      V_ACUMULADOS.ID_DE_EMPLEADO, 
                                      V_ACUMULADOS.NUMERO_DCTO,
                                      V_ACUMULADOS.NOMBRECOMPLETO,
                                      SUM(V_ACUMULADOS.VALOR) VALOR,
                                      V_ACUMULADOS.ID_DE_CONCEPTO
                               FROM  V_ACUMULADOS
                               WHERE V_ACUMULADOS.ID_DE_CONCEPTO = 130 
                               GROUP BY V_ACUMULADOS.ID_DE_EMPLEADO, V_ACUMULADOS.COMPANIA, V_ACUMULADOS.ID_DE_CONCEPTO, V_ACUMULADOS.ANO, V_ACUMULADOS.NUMERO_DCTO,
                                      V_ACUMULADOS.NOMBRECOMPLETO
                               )
SELECT  NOVEDADES.ID_DE_EMPLEADO,
        ACUMULADO_NOVEDAD_130.NOMBRECOMPLETO,
        ACUMULADO_NOVEDAD_130.ANO,
        ACUMULADO_NOVEDAD_130.NUMERO_DCTO,
        ACUMULADO_NOVEDAD_130.ID_DE_CONCEPTO,
        ACUMULADO_NOVEDAD_130.VALOR SUMATORIA,
        NOVEDADES.VALOR CN304
FROM  NOVEDADES
     INNER JOIN ACUMULADO_NOVEDAD_130
      ON  NOVEDADES.COMPANIA       = ACUMULADO_NOVEDAD_130.COMPANIA
      AND NOVEDADES.ID_DE_EMPLEADO = ACUMULADO_NOVEDAD_130.ID_DE_EMPLEADO
WHERE NOVEDADES.COMPANIA         = 's$compania$s'   
 AND ACUMULADO_NOVEDAD_130.ANO = s$anio$s
 AND NOVEDADES.ID_DE_CONCEPTO    = 304 
ORDER BY 2
