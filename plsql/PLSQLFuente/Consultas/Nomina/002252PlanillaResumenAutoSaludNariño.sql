SELECT
	V_ACUMULADOS.FONDO_SALUD AS FONDO_SALUD,
	MAX(V_ACUMULADOS.NUMERO_DCTO) KEEP (DENSE_RANK LAST
ORDER BY
	ROWNUM) AS CEDULA,
	V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD,
	MIN(V_ACUMULADOS.SALARIO_BASE_IBC) KEEP (DENSE_RANK FIRST
ORDER BY
	ROWNUM) AS SALARIO_BASE_IBC,
	MAX(V_ACUMULADOS.ID_DE_PROCESO) KEEP (DENSE_RANK LAST
ORDER BY
	ROWNUM) AS PROCESO,
	MAX(V_ACUMULADOS.ANO) KEEP (DENSE_RANK LAST
ORDER BY
	ROWNUM) AS ANO,
	PCK_SYSMAN_UTL.FC_NOMBRE_MES(MAX(V_ACUMULADOS.MES) KEEP (DENSE_RANK LAST
ORDER BY
	ROWNUM)) AS MES,
	MAX(V_ACUMULADOS.ID_DE_CARGO) KEEP (DENSE_RANK LAST
ORDER BY
	ROWNUM) AS CARGO,
	MAX(V_ACUMULADOS.DEPENDENCIA) KEEP (DENSE_RANK LAST
ORDER BY
	ROWNUM) AS DEPENDENCIA,
	MAX(V_ACUMULADOS.PERIODO) KEEP (DENSE_RANK LAST
ORDER BY
	ROWNUM) AS PERIODO,
	MAX(V_ACUMULADOS.NOMBRECOMPLETO) KEEP (DENSE_RANK LAST
ORDER BY
	ROWNUM) AS NOM_EMPLEADO,
	Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 1 THEN V_ACUMULADOS.VALOR ELSE 0 END )) AS C001,
	Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 73 THEN V_ACUMULADOS.VALOR ELSE 0 END )) AS C073,
	Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 186 THEN V_ACUMULADOS.VALOR ELSE 0 END )) AS C186,
	Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 399 THEN V_ACUMULADOS.VALOR ELSE 0 END ))+ Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 398 THEN V_ACUMULADOS.VALOR ELSE 0 END )) AS DTOMATERNIDAD,
	Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 379 THEN V_ACUMULADOS.VALOR ELSE 0 END )) AS INCAPACIDADES,
	Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 113 THEN V_ACUMULADOS.VALOR ELSE 0 END ))+ Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 116 THEN V_ACUMULADOS.VALOR ELSE 0 END )) AS SALUD,
	Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 114 THEN V_ACUMULADOS.VALOR ELSE 0 END ))+ Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 119 THEN V_ACUMULADOS.VALOR ELSE 0 END )) AS GARANTIA,
	Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 114 THEN V_ACUMULADOS.VALOR ELSE 0 END ))+ Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 119 THEN V_ACUMULADOS.VALOR ELSE 0 END ))+ Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 123 THEN V_ACUMULADOS.VALOR ELSE 0 END ))+ Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 486 THEN V_ACUMULADOS.VALOR ELSE 0 END ))+ Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 113 THEN V_ACUMULADOS.VALOR ELSE 0 END ))+ Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 116 THEN V_ACUMULADOS.VALOR ELSE 0 END )) AS TOTALSALUD,
	Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 112 THEN V_ACUMULADOS.VALOR ELSE 0 END )) AS C112,
	Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 113 THEN V_ACUMULADOS.VALOR ELSE 0 END )) AS C113,
	Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 116 THEN V_ACUMULADOS.VALOR ELSE 0 END )) AS C116,
	Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 123 THEN V_ACUMULADOS.VALOR ELSE 0 END )) AS C123,
	Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 486 THEN V_ACUMULADOS.VALOR ELSE 0 END )) AS C486,
	Sum(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 114 THEN V_ACUMULADOS.VALOR ELSE 0 END )) AS C114,
	SUM(( CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO = 119 THEN V_ACUMULADOS.VALOR ELSE 0 END )) AS C119,
	V_FONDO_DE_SALUD.CODAIFS,
	V_FONDO_DE_SALUD.NIT
FROM
	V_ACUMULADOS
INNER JOIN V_FONDO_DE_SALUD ON
	V_ACUMULADOS.COMPANIA = V_FONDO_DE_SALUD.COMPANIA
	AND V_ACUMULADOS.FONDO_SALUD = V_FONDO_DE_SALUD.FONDO_SALUD
WHERE
	V_ACUMULADOS.COMPANIA = s$compania$s
	AND V_ACUMULADOS.ID_DE_PROCESO = s$proceso$s
	AND V_ACUMULADOS.ANO = s$ano$s
	AND V_ACUMULADOS.MES = s$mes$s
	AND V_ACUMULADOS.ID_DE_CONCEPTO IN (1, 73, 111, 112, 113, 114, 115, 116, 117, 118, 119, 123, 186, 379, 398, 399, 486)
GROUP BY
	V_ACUMULADOS.FONDO_SALUD ,
	V_ACUMULADOS.NUMERO_DCTO ,
	V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD,
	V_ACUMULADOS.SALARIO_BASE_IBC ,
	V_ACUMULADOS.ID_DE_PROCESO,
	V_ACUMULADOS.ANO,
	V_ACUMULADOS.MES,
	V_ACUMULADOS.ID_DE_CARGO,
	V_ACUMULADOS.DEPENDENCIA,
	V_ACUMULADOS.PERIODO,
	V_ACUMULADOS.NOMBRECOMPLETO,
	V_FONDO_DE_SALUD.CODAIFS,
	V_FONDO_DE_SALUD.NIT
ORDER BY
	V_ACUMULADOS.FONDO_SALUD,
	V_ACUMULADOS.NUMERO_DCTO 