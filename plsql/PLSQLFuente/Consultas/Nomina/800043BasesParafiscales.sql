WITH PARAF1 AS 
(SELECT   HISTORICOS.COMPANIA, 
          PERSONAL.NUMERO_DCTO, 
          HISTORICOS.ID_DE_PROCESO, 
          HISTORICOS.ANO, 
          HISTORICOS.MES, 
          PERSONAL.APELLIDO1, 
          PERSONAL.APELLIDO2, 
          PERSONAL.NOMBRES, 
          HISTORICOS.ID_DE_EMPLEADO, 
          SUM(HISTORICOS.VALOR) VALOR
    FROM  PERSONAL 
      INNER JOIN HISTORICOS 
        ON  PERSONAL.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO 
        AND PERSONAL.COMPANIA           = HISTORICOS.COMPANIA
    WHERE HISTORICOS.COMPANIA            = s$compania$s 
      AND HISTORICOS.ANO                  	      = s$anioNomina$s 
      AND HISTORICOS.MES                  	      = s$mesNomina$s
      AND HISTORICOS.PERIODO          	      NOT IN(05)
      AND HISTORICOS.ID_DE_CONCEPTO   =  s$concepto$s
    GROUP BY  HISTORICOS.COMPANIA, 
            PERSONAL.NUMERO_DCTO, 
            HISTORICOS.ID_DE_PROCESO, 
            HISTORICOS.ANO, 
            HISTORICOS.MES, 
            PERSONAL.APELLIDO1, 
            PERSONAL.APELLIDO2, 
            PERSONAL.NOMBRES, 
            HISTORICOS.ID_DE_EMPLEADO
      ORDER BY  PERSONAL.NUMERO_DCTO, 
            PERSONAL.APELLIDO1, 
            PERSONAL.APELLIDO2, 
            PERSONAL.NOMBRES)
SELECT HISTORICOS.COMPANIA, 
    HISTORICOS.ID_DE_PROCESO, 
    TO_CHAR(HISTORICOS.ANO) ANO, 
    HISTORICOS.MES, 
    PERSONAL.NUMERO_DCTO NUMERO_DCTO, 
    PERSONAL.APELLIDO1, 
    PERSONAL.APELLIDO2, 
    PERSONAL.NOMBRES, 
    HISTORICOS.PERIODO, 
    HISTORICOS.ID_DE_EMPLEADO, 
    PARAF1.VALOR VALORMES, 
    SUM(HISTORICOS.VALOR) SUMADEVALOR, 
    SUM(HISTORICOS.VALOR+0+PARAF1.VALOR) TOT_CONRETRO, 
    PERSONAL.FONDO_SALUD, PERSONAL.ESCALAFON
FROM  (HISTORICOS 
      LEFT JOIN PARAF1 
        ON HISTORICOS.ID_DE_EMPLEADO  = PARAF1.ID_DE_EMPLEADO
        AND HISTORICOS.MES                       = PARAF1.MES
        AND HISTORICOS.ANO                      = PARAF1.ANO
        AND HISTORICOS.ID_DE_PROCESO  = PARAF1.ID_DE_PROCESO
        AND HISTORICOS.COMPANIA            = PARAF1.COMPANIA)
        LEFT JOIN PERSONAL 
           ON  HISTORICOS.ID_DE_EMPLEADO  = PERSONAL.ID_DE_EMPLEADO 
          AND HISTORICOS.COMPANIA             = PERSONAL.COMPANIA
WHERE HISTORICOS.COMPANIA            = s$compania$s 
  AND HISTORICOS.ANO              	 = s$anioNomina$s 
  AND HISTORICOS.MES              	 = s$mesNomina$s 
  AND HISTORICOS.PERIODO          	 = '05' 
  AND HISTORICOS.ID_DE_CONCEPTO  = s$concepto$s
  AND PERSONAL.ESCALAFON          	 < '05'
GROUP BY  HISTORICOS.COMPANIA, 
      HISTORICOS.ID_DE_PROCESO, 
      HISTORICOS.ANO, 
      HISTORICOS.MES, 
      PERSONAL.NUMERO_DCTO, 
      PERSONAL.APELLIDO1, 
      PERSONAL.APELLIDO2, 
      PERSONAL.NOMBRES, 
      HISTORICOS.PERIODO, 
      HISTORICOS.ID_DE_EMPLEADO, 
      PARAF1.VALOR, 
      PERSONAL.FONDO_SALUD, 
      PERSONAL.ESCALAFON
ORDER BY  PERSONAL.APELLIDO1, 
      PERSONAL.APELLIDO2, 
      PERSONAL.NOMBRES
