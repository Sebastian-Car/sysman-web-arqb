SELECT DISTINCT 
    TO_CHAR(PERIODOS.FECHAFINAL,'DD/MM/YYYY') FECHAFINAL,
    CONCEPTOS.NOMBRE_CONCEPTO NOMBRECONCEPTO, 
    PERSONAL.APELLIDO1 || ' ' ||PERSONAL.APELLIDO2 || ' ' ||PERSONAL.NOMBRES NOMBRECOMPLETO, 
    PERSONAL.NUMERO_DCTO NUMERODCTO,   
    SUM(FINANCIABLES_DE_NOMINA.VALOR_CUOTA) SUMADEVALORCUOTA,  
    SUM(FINANCIABLES_DE_NOMINA.SALDO) SUMADESALDO,
    FINANCIABLES_DE_NOMINA.ANO,
    FINANCIABLES_DE_NOMINA.MES,
    FINANCIABLES_DE_NOMINA.PERIODO,
    MIN(CONCEPTOS.ID_DE_CONCEPTO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) PRIMERCONCEPTO
FROM FINANCIABLES_DE_NOMINA 
LEFT JOIN PERIODOS 
    ON  FINANCIABLES_DE_NOMINA.COMPANIA = PERIODOS.COMPANIA 
    AND FINANCIABLES_DE_NOMINA.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO   
    AND FINANCIABLES_DE_NOMINA.ANO = PERIODOS.ANO 
    AND FINANCIABLES_DE_NOMINA.MES = PERIODOS.MES
    AND FINANCIABLES_DE_NOMINA.PERIODO = PERIODOS.PERIODO 
LEFT JOIN CONCEPTOS 
ON FINANCIABLES_DE_NOMINA.COMPANIA = CONCEPTOS.COMPANIA 
AND FINANCIABLES_DE_NOMINA.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO 
LEFT JOIN PERSONAL 
ON FINANCIABLES_DE_NOMINA.COMPANIA = PERSONAL.COMPANIA  
AND  FINANCIABLES_DE_NOMINA.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
GROUP BY TO_CHAR(PERIODOS.FECHAFINAL,'DD/MM/YYYY'),
    CONCEPTOS.NOMBRE_CONCEPTO, 
    PERSONAL.APELLIDO1 || ' ' ||PERSONAL.APELLIDO2 || ' ' ||PERSONAL.NOMBRES,
    PERSONAL.NUMERO_DCTO,   
    FINANCIABLES_DE_NOMINA.VALOR_CUOTA,  
    FINANCIABLES_DE_NOMINA.SALDO,
    FINANCIABLES_DE_NOMINA.ANO,
    FINANCIABLES_DE_NOMINA.MES,
    FINANCIABLES_DE_NOMINA.COMPANIA,
    FINANCIABLES_DE_NOMINA.PERIODO
HAVING FINANCIABLES_DE_NOMINA.COMPANIA = s$compania$s
AND FINANCIABLES_DE_NOMINA.ANO = s$anioNomina$s
AND FINANCIABLES_DE_NOMINA.MES = s$mesNomina$s 
AND FINANCIABLES_DE_NOMINA.PERIODO = s$periodoNomina$s 
ORDER BY MIN(CONCEPTOS.ID_DE_CONCEPTO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM)
