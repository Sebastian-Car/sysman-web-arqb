SELECT 
HISTORICOS.ID_DE_PROCESO,
HISTORICOS.ANO,
UPPER(PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES)) MES,
HISTORICOS.PERIODO,
HISTORICOS.ID_DE_EMPLEADO,
(PERSONAL.APELLIDO1 || ' ' || PERSONAL.APELLIDO2 || ' ' || PERSONAL.NOMBRES) NOMCOMPLETO, 
PERSONAL.NUMERO_DCTO,
PERSONAL.FECHA_DE_INGRESO,
PERSONAL.ID_CENTRO_DE_COSTO,
DEPENDENCIA.CODIGO DEPENDENCIA,
DEPENDENCIA.NOMBRE NOMBRE_DEPENDENCIA,
PERSONAL.ID_DE_CARGO,
CENTRO_COSTO.NOMBRE NOMBRE_CENTRO_DE_COSTO, 
SUM(CASE WHEN 
		HISTORICOS.ID_DE_CONCEPTO = 160
	THEN 
		VALOR
	ELSE
		0
	END)  PRIMAJUN,  
SUM(CASE WHEN 
		HISTORICOS.ID_DE_CONCEPTO = 945
	THEN 
		VALOR
	ELSE
		0
	END)  SUELDOJUN,  
SUM(CASE WHEN 
		HISTORICOS.ID_DE_CONCEPTO = 950
	THEN 
		VALOR
	ELSE
		0
	END)  GASTOSREP,  
SUM(CASE WHEN 
		HISTORICOS.ID_DE_CONCEPTO = 946
	THEN 
		VALOR
	ELSE
		0
	END)  RECARGOSJUN,     
SUM(CASE WHEN 
		HISTORICOS.ID_DE_CONCEPTO = 948
	THEN 
		VALOR
	ELSE
		0
	END)  TRANSPJUN,  
SUM(CASE WHEN 
		HISTORICOS.ID_DE_CONCEPTO = 949
	THEN 
		VALOR
	ELSE
		0
	END)  ALIMENJUN,  
SUM(CASE WHEN 
		HISTORICOS.ID_DE_CONCEPTO = 950
	THEN 
		VALOR
	ELSE
		0
	END) PLOCALJUN,  
SUM(CASE WHEN 
		HISTORICOS.ID_DE_CONCEPTO = 951
	THEN 
		VALOR
	ELSE
		0
	END)  DIASPJUN,  
SUM(CASE WHEN 
		HISTORICOS.ID_DE_CONCEPTO = 952
	THEN 
		VALOR
	ELSE
		0
	END)  DIASCOMJUN,  
SUM(CASE WHEN 
		HISTORICOS.ID_DE_CONCEPTO = 953
	THEN 
		VALOR
	ELSE
		0
	END)  DNT, 		
SUM(CASE WHEN 
		HISTORICOS.ID_DE_CONCEPTO = 140
	THEN 
		VALOR
	ELSE
		0
	END)  DESCUENTOS,  
SUM(CASE WHEN 
		HISTORICOS.ID_DE_CONCEPTO = 144
	THEN 
		VALOR
	ELSE
		0
	END)  NETO,  
CARGOS.NOMBRE_DEL_CARGO,
PERSONAL.ID_DE_TIPO,  
SUM(CASE WHEN 
		HISTORICOS.ID_DE_CONCEPTO = 67
	THEN 
		VALOR
	ELSE
		0
	END)  DIASPROP,  
SUM(CASE WHEN 
		HISTORICOS.ID_DE_CONCEPTO = 946 
	THEN 
		VALOR
	ELSE
		0
	END)  BON   
FROM HISTORICOS
INNER JOIN PERSONAL
	ON HISTORICOS.COMPANIA          = PERSONAL.COMPANIA
	AND HISTORICOS.ID_DE_EMPLEADO   = PERSONAL.ID_DE_EMPLEADO
INNER JOIN CENTRO_COSTO
	ON PERSONAL.COMPANIA            = CENTRO_COSTO.COMPANIA
	AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
	AND PERSONAL.ANO  				= CENTRO_COSTO.ANO
INNER JOIN DEPENDENCIA
	ON PERSONAL.COMPANIA   			= DEPENDENCIA.COMPANIA
	AND PERSONAL.DEPENDENCIA 		= DEPENDENCIA.CODIGO
INNER JOIN CARGOS
	ON PERSONAL.COMPANIA         	= CARGOS.COMPANIA
	AND  PERSONAL.ID_DE_CARGO       = CARGOS.ID_DE_CARGO
WHERE PERSONAL.COMPANIA         = s$compania$s  
AND HISTORICOS.ID_DE_PROCESO    = s$idProceso$s  
AND HISTORICOS.ANO              = s$ano$s 
AND HISTORICOS.MES              = s$mes$s  
AND HISTORICOS.PERIODO          = s$periodo$s  
AND PERSONAL.ID_DE_TIPO NOT IN (2)
GROUP BY HISTORICOS.ID_DE_PROCESO,
HISTORICOS.ANO,
HISTORICOS.MES,
HISTORICOS.PERIODO,
HISTORICOS.ID_DE_EMPLEADO,
(PERSONAL.APELLIDO1 || ' ' || PERSONAL.APELLIDO2 || ' ' || PERSONAL.NOMBRES),
PERSONAL.NUMERO_DCTO,
PERSONAL.FECHA_DE_INGRESO,
PERSONAL.ID_CENTRO_DE_COSTO,
DEPENDENCIA.CODIGO,
DEPENDENCIA.NOMBRE,
PERSONAL.ID_DE_CARGO,
CENTRO_COSTO.NOMBRE, 
CARGOS.NOMBRE_DEL_CARGO,
PERSONAL.ID_DE_TIPO  
HAVING  SUM(CASE WHEN 
		HISTORICOS.ID_DE_CONCEPTO = 160
	THEN 
		VALOR
	ELSE
		0
	END) > 0