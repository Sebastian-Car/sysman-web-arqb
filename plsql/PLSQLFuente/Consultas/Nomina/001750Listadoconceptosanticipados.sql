SELECT 
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  HISTORICOS.ID_DE_EMPLEADO,
  HISTORICOS.ID_DE_CONCEPTO,
  HISTORICOS.FECHA,
  HISTORICOS.VALOR,
  HISTORICOS.OBSERVACIONES,
  CONCEPTOS.CLASE,
  CONCEPTOS.NOMBRE_CONCEPTO AS NOMBRE_CONCEPTO,
  PERSONAL.NOMBRECOMPLETO,
  PERSONAL.NUMERO_DCTO,
  HISTORICOS.VALOR AS VALOR1,
  CASE HISTORICOS.ID_DE_CONCEPTO
    WHEN 137
    THEN V_FONDO_DE_SALUD.NOMBRE_FONDO_SALUD
    ELSE V_FONDO_DE_PENSIONES.NOMBRE_DEL_FONDO
  END AS NOMBREFONDO
FROM HISTORICOS
  INNER JOIN PERSONAL
     ON HISTORICOS.COMPANIA       = PERSONAL.COMPANIA
    AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
  INNER JOIN CONCEPTOS
     ON HISTORICOS.COMPANIA        = CONCEPTOS.COMPANIA
    AND HISTORICOS.ID_DE_CONCEPTO  = CONCEPTOS.ID_DE_CONCEPTO
  INNER JOIN V_FONDO_DE_SALUD
     ON PERSONAL.COMPANIA          = V_FONDO_DE_SALUD.COMPANIA
    AND PERSONAL.FONDO_SALUD       = V_FONDO_DE_SALUD.FONDO_SALUD
  INNER JOIN V_FONDO_DE_PENSIONES
     ON PERSONAL.COMPANIA          = V_FONDO_DE_PENSIONES.COMPANIA
    AND PERSONAL.ID_DEL_FONDO      = V_FONDO_DE_PENSIONES.ID_DEL_FONDO
WHERE HISTORICOS.COMPANIA        = s$compania$s
  AND HISTORICOS.ID_DE_PROCESO   = s$proceso$s
  AND HISTORICOS.ANO             = s$ano1$s
  AND HISTORICOS.MES             = s$mes1$s
  AND HISTORICOS.PERIODO         = s$periodo1$s
