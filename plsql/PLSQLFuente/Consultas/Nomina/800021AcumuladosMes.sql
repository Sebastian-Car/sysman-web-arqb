SELECT *  
 FROM  
 (SELECT V_ACUMULADOS.ID_DE_EMPLEADO CÓDIGO,
         PERSONAL_HISTORICO.ESTADO_ACTUAL ESTADO,
         PERSONAL_HISTORICO.ID_CENTRO_DE_COSTO CÓD_CENTRO_COSTO ,
         PERSONAL_HISTORICO.NOMBRE_CENTRO_DE_COSTO CENTRO_COSTO ,
         PERSONAL_HISTORICO.DIRECCION ,
         PERSONAL_HISTORICO.NUMERO_DCTO CÉDULA,
         PERSONAL_HISTORICO.NOMBRECOMPLETO NOMBRE_EMPLEADO,
         PERSONAL_HISTORICO.ID_DE_CARGO CÓD_CARGO, 
         PERSONAL_HISTORICO.NOMBRE_DE_CARGO NOMBRE_CARGO,
         SUM(V_ACUMULADOS.VALOR)  TOTAL,  
         V_ACUMULADOS.CLASE || '_' || V_ACUMULADOS.ID_DE_CONCEPTO PER
      FROM   V_ACUMULADOS  
       INNER JOIN PERSONAL_HISTORICO 
         ON (V_ACUMULADOS.COMPANIA = PERSONAL_HISTORICO.COMPANIA)
        AND (V_ACUMULADOS.ID_DE_PROCESO = PERSONAL_HISTORICO.ID_DE_PROCESO)
        AND (V_ACUMULADOS.ANO = PERSONAL_HISTORICO.ANO)
        AND (V_ACUMULADOS.MES = PERSONAL_HISTORICO.MES)
        AND (V_ACUMULADOS.PERIODO = PERSONAL_HISTORICO.PERIODO)
        AND (V_ACUMULADOS.ID_DE_EMPLEADO = PERSONAL_HISTORICO.ID_DE_EMPLEADO)        
      WHERE  V_ACUMULADOS.COMPANIA = s$compania$s
        AND  V_ACUMULADOS.ANO = s$ano$s
        AND  V_ACUMULADOS.MES = s$mes$s
        AND  V_ACUMULADOS.CLASE IN (3,5,8)
        AND  V_ACUMULADOS.VALOR NOT IN(0) 
      GROUP BY V_ACUMULADOS.ID_DE_EMPLEADO,
         PERSONAL_HISTORICO.ESTADO_ACTUAL,
         PERSONAL_HISTORICO.ID_CENTRO_DE_COSTO  ,
         PERSONAL_HISTORICO.NOMBRE_CENTRO_DE_COSTO  ,
         PERSONAL_HISTORICO.DIRECCION,
         PERSONAL_HISTORICO.NUMERO_DCTO,
         PERSONAL_HISTORICO.NOMBRECOMPLETO,
         PERSONAL_HISTORICO.ID_DE_CARGO,
         PERSONAL_HISTORICO.NOMBRE_DE_CARGO,
         V_ACUMULADOS.CLASE || '_' ||  V_ACUMULADOS.ID_DE_CONCEPTO
  ) PIVOTHISTORICOS  
 PIVOT ( SUM(TOTAL) FOR PER IN ('s$condicionPivot$s))  
 ORDER BY 2
