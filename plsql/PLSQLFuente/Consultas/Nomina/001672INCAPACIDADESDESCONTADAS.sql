SELECT NOVEDADES_AUTOLIQUIDACION.ANO,
  NOVEDADES_AUTOLIQUIDACION.MES,
  NOVEDADES_AUTOLIQUIDACION.CODIGO_FONDO,
  NOVEDADES_AUTOLIQUIDACION.NIT,
  NOVEDADES_AUTOLIQUIDACION.NOMBRE_FONDO,
  PERIODOS.FECHAFINAL,
  NOVEDADES_AUTOLIQUIDACION.NUM_AUTO_INCAP,
  NOVEDADES_AUTOLIQUIDACION.VR_INCAP,
  NOVEDADES_AUTOLIQUIDACION.NUM_AUTO_MAT,
  NOVEDADES_AUTOLIQUIDACION.VR_MAT
FROM NOVEDADES_AUTOLIQUIDACION
  LEFT JOIN PERIODOS
    ON NOVEDADES_AUTOLIQUIDACION.COMPANIA = PERIODOS.COMPANIA 
   AND NOVEDADES_AUTOLIQUIDACION.ANO      = PERIODOS.ANO
   AND NOVEDADES_AUTOLIQUIDACION.PERIODO  = PERIODOS.PERIODO
   AND NOVEDADES_AUTOLIQUIDACION.MES      = PERIODOS.MES
    INNER JOIN V_PERSONAL
      ON NOVEDADES_AUTOLIQUIDACION.COMPANIA      = V_PERSONAL.COMPANIA
      AND NOVEDADES_AUTOLIQUIDACION.ANO          = V_PERSONAL.ANO
      AND NOVEDADES_AUTOLIQUIDACION.MES          = V_PERSONAL.MES
      AND NOVEDADES_AUTOLIQUIDACION.PERIODO      = V_PERSONAL.PERIODO
      AND NOVEDADES_AUTOLIQUIDACION.CODIGO_FONDO = V_PERSONAL.FONDO_SALUD
WHERE V_PERSONAL.COMPANIA                  = s$compania$s
AND V_PERSONAL.ID_DE_PROCESO               = s$procesoNomina$s
AND V_PERSONAL.ANO                         = s$anoNomina$s
AND V_PERSONAL.MES                         = s$mesNomina$s
AND V_PERSONAL.PERIODO                     = s$periodoNomina$s
AND V_PERSONAL.ID_DE_EMPLEADO NOT         IN (0)
AND V_PERSONAL.ESTADO_ACTUAL               = 's$estadoActual$s'
AND V_PERSONAL.SEXO                        = CASE
                                              WHEN 's$genero$s' = 'T'
                                              THEN V_PERSONAL.SEXO
                                              ELSE 's$genero$s'
                                            END
AND V_PERSONAL.ID_DE_CARGO BETWEEN 's$cargoInicial$s' AND 's$cargoFinal$s'
AND V_PERSONAL.DE_CARRERA BETWEEN 's$tipoContratoInicial$s' AND 's$tipoContratoFinal$s'
AND V_PERSONAL.FECHANCTO BETWEEN TO_DATE('s$fechaInicialNacimiento$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinalNacimiento$s','DD/MM/YYYY')
AND NOVEDADES_AUTOLIQUIDACION.TIPO_ADMINISTRADORA IN ('02')
AND NOVEDADES_AUTOLIQUIDACION.CODIGO_FONDO BETWEEN 's$epsInicial$s' AND 's$epsFinal$s'
AND PERIODOS.FECHAFINAL BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
s$condicion$s
