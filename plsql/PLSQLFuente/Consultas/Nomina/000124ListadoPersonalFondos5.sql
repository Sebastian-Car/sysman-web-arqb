SELECT DISTINCT
        PERSONAL.NOMBRECOMPLETO AS NOMCOMPLETO, 
        PERSONAL.ID_DE_EMPLEADO,
        PERSONAL.ESTADO_ACTUAL,
        V_MEDICINA_PREPAGADA.MEDICINA_PREPAGADA AS IDFONDO, 
        V_MEDICINA_PREPAGADA.NIT, 
        V_MEDICINA_PREPAGADA.NOMBRE_MEDICINA AS NOMBREFONDO, 
        PERSONAL.NUMERO_DCTO, 
        PERSONAL.FECHAMEDICINA AS FECHAFONDO
   FROM PERSONAL 
   LEFT JOIN V_MEDICINA_PREPAGADA ON 
   PERSONAL.COMPANIA = V_MEDICINA_PREPAGADA.COMPANIA AND 
   PERSONAL.MEDICINA_PREPAGADA = V_MEDICINA_PREPAGADA.MEDICINA_PREPAGADA
   WHERE PERSONAL.ID_DE_EMPLEADO NOT IN (0)
   AND PERSONAL.ESTADO_ACTUAL NOT IN (3)
   AND PERSONAL.COMPANIA = s$compania$s
