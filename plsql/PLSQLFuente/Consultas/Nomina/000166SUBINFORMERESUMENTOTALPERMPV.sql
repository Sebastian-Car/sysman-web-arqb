SELECT
ACUM_DANE_NIVELES_INI.NOMBREESCALAFON AS NIVEL2, 
ACUM_DANE_NIVELES_INI.ESCALAFON AS ESCAL2,                 
Sum(ACUM_DANE_NIVELES_INI.SUELDO_PERMAIND_M) AS PERMANENTE_MUJER2,
Sum(ACUM_DANE_NIVELES_INI.SUELDO_PERMAIND_H) AS PERMANENTE_HOMBRE2, 
Sum(ACUM_DANE_NIVELES_INI.TOTALSUELDOPERM) AS PERMANENTE_TOTAL2,          
Sum(ACUM_DANE_NIVELES_INI.SUELDO_PERMAFIJO_M) AS FIJO_MUJER2, 
Sum(ACUM_DANE_NIVELES_INI.SUELDO_PERMAFIJO_H) AS FIJO_HOMBRE2, 
Sum(ACUM_DANE_NIVELES_INI.TOTALSUELDOFIJO) AS FIJO_TOTAL2,         
Sum(ACUM_DANE_NIVELES_INI.GASTO_APRENDIZSENA_M) AS SENA_MUJER2, 
Sum(ACUM_DANE_NIVELES_INI.GASTO_APRENDIZSENA_H) AS SENA_HOMBRE2, 
Sum(ACUM_DANE_NIVELES_INI.TOTALSUELDOAPRENDIZ) AS SENA_TOTAL2,
Sum(ACUM_DANE_NIVELES_INI.TOT) AS TOTAL2
        FROM (
        SELECT 
        PERIODOS.ID_DE_PROCESO, 
                ESCALAFON.NOMBRE AS NOMBREESCALAFON, 
                ACUM_DANE_DISTIC.ESCALAFON AS ESCALAFON,
                Sum(Historicos.VALOR) AS TOT,
                Sum(CASE WHEN ACUM_DANE_DISTIC.sexo='M' And ACUM_DANE_DISTIC.DE_CARRERA='CI'And ACUM_DANE_DISTIC.ESCALAFON<>'13' THEN HISTORICOS.VALOR ELSE 0 END) AS SUELDO_PERMAIND_H,
                Sum(CASE WHEN ACUM_DANE_DISTIC.sexo='F' And ACUM_DANE_DISTIC.DE_CARRERA='CI' And ACUM_DANE_DISTIC.ESCALAFON<>'13' THEN HISTORICOS.VALOR ELSE 0 END) AS SUELDO_PERMAIND_M,
                Sum(CASE WHEN ACUM_DANE_DISTIC.sexo='M' And ACUM_DANE_DISTIC.DE_CARRERA='CI'And ACUM_DANE_DISTIC.ESCALAFON<>'13' THEN HISTORICOS.VALOR ELSE 0 END) + Sum(CASE WHEN ACUM_DANE_DISTIC.sexo='F' And ACUM_DANE_DISTIC.DE_CARRERA='CI' And ACUM_DANE_DISTIC.ESCALAFON<>'13' THEN HISTORICOS.VALOR ELSE 0 END) AS TOTALSUELDOPERM,
                Sum(CASE WHEN ACUM_DANE_DISTIC.sexo='M' And ACUM_DANE_DISTIC.DE_CARRERA<>'CI' And ACUM_DANE_DISTIC.ESCALAFON<>'13' THEN HISTORICOS.VALOR ELSE 0 END) AS SUELDO_PERMAFIJO_H,
                Sum(CASE WHEN ACUM_DANE_DISTIC.sexo='F' And ACUM_DANE_DISTIC.DE_CARRERA<>'CI' And ACUM_DANE_DISTIC.ESCALAFON<>'13' THEN HISTORICOS.VALOR ELSE 0 END) AS SUELDO_PERMAFIJO_M,
                Sum(CASE WHEN ACUM_DANE_DISTIC.sexo='M' And ACUM_DANE_DISTIC.DE_CARRERA<>'CI' And ACUM_DANE_DISTIC.ESCALAFON<>'13' THEN HISTORICOS.VALOR ELSE 0 END) + Sum(CASE WHEN ACUM_DANE_DISTIC.sexo='F' And ACUM_DANE_DISTIC.DE_CARRERA<>'CI' And ACUM_DANE_DISTIC.ESCALAFON<>'13' THEN HISTORICOS.VALOR ELSE 0 END) AS TOTALSUELDOFIJO,
                Sum(CASE WHEN ACUM_DANE_DISTIC.sexo='M' And ACUM_DANE_DISTIC.ESCALAFON='13' THEN HISTORICOS.VALOR ELSE 0 END) AS GASTO_APRENDIZSENA_H,
                Sum(CASE WHEN ACUM_DANE_DISTIC.sexo='F' And ACUM_DANE_DISTIC.ESCALAFON='13' THEN HISTORICOS.VALOR ELSE 0 END) AS GASTO_APRENDIZSENA_M,
                Sum(CASE WHEN ACUM_DANE_DISTIC.sexo='M' And ACUM_DANE_DISTIC.ESCALAFON='13' THEN HISTORICOS.VALOR ELSE 0 END) + Sum(CASE WHEN ACUM_DANE_DISTIC.sexo='F' And ACUM_DANE_DISTIC.ESCALAFON='13' THEN HISTORICOS.VALOR ELSE 0 END) AS TOTALSUELDOAPRENDIZ 
                  FROM ((PERIODOS INNER JOIN
                  (CONCEPTOS INNER JOIN HISTORICOS ON 
                  (CONCEPTOS.COMPANIA = HISTORICOS.COMPANIA) AND 
                  (CONCEPTOS.ID_DE_CONCEPTO = HISTORICOS.ID_DE_CONCEPTO)) ON 
                  (PERIODOS.COMPANIA = HISTORICOS.COMPANIA) AND 
                  (PERIODOS.ID_DE_PROCESO = HISTORICOS.ID_DE_PROCESO) AND 
                  (PERIODOS.ANO = HISTORICOS.ANO) AND 
                  (PERIODOS.MES = HISTORICOS.MES) AND 
                  (PERIODOS.PERIODO = HISTORICOS.PERIODO))
                  LEFT JOIN (SELECT 
                        PERSONAL.COMPANIA,  
                        PERSONAL.ID_DE_EMPLEADO,
                        MIN(PERSONAL.ESCALAFON) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) AS ESCALAFON, 
                        MIN(PERSONAL.DE_CARRERA) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) AS DE_CARRERA,
                        PERSONAL.SEXO
                        FROM PERSONAL LEFT JOIN 
                        ESCALAFON ON 
                        (PERSONAL.ESCALAFON = ESCALAFON.CODIGO) AND 
                        (PERSONAL.COMPANIA = ESCALAFON.COMPANIA) 
                        WHERE PERSONAL.COMPANIA IN (s$compania$s)
                        GROUP BY 
                        PERSONAL.COMPANIA, 
                        PERSONAL.ID_DE_EMPLEADO, 
                        PERSONAL.SEXO) ACUM_DANE_DISTIC ON 
                  (HISTORICOS.COMPANIA = ACUM_DANE_DISTIC.COMPANIA) AND
                  (HISTORICOS.ID_DE_EMPLEADO = ACUM_DANE_DISTIC.ID_DE_EMPLEADO))
                  LEFT JOIN ESCALAFON ON 
                  (ACUM_DANE_DISTIC.COMPANIA = ESCALAFON.COMPANIA) AND 
                  (ACUM_DANE_DISTIC.ESCALAFON = ESCALAFON.CODIGO)
                  WHERE PERIODOS.ACUMULADO NOT IN (0)
                    AND CONCEPTOS.CLASE IN(4)
                    AND HISTORICOS.COMPANIA IN(s$compania$s) 
                    AND HISTORICOS.ANO || LPAD(HISTORICOS.MES,2,'0') || LPAD(HISTORICOS.PERIODO,2,'0')  
         BETWEEN s$periodo1$s AND s$periodo2$s
                    AND ACUM_DANE_DISTIC.ID_DE_EMPLEADO NOT IN (0)
                    GROUP BY ESCALAFON.NOMBRE, 
                    ACUM_DANE_DISTIC.ESCALAFON, 
                    PERIODOS.ID_DE_PROCESO 
                    ORDER BY ACUM_DANE_DISTIC.ESCALAFON) 
        ACUM_DANE_NIVELES_INI  
                s$COND1$s 
                    GROUP BY ACUM_DANE_NIVELES_INI.NOMBREESCALAFON, ACUM_DANE_NIVELES_INI.ESCALAFON
                    ORDER BY NIVEL2
