SELECT PERSONAL.NOMBRECOMPLETO,
  CARGOS.NOMBRE_DEL_CARGO      AS NOMBRE_DEL_CARGO,
  PERSONAL.NUMERO_DCTO         AS NUMERO_DCTO,
  PERSONAL.FECHA_DE_INGRESO,
  PERSONAL.FECHA_INICIO,
  PERSONAL.FECHA_FINAL,
  PERSONAL.INICIO_DISFRUTE,
  PERSONAL.FINAL_DISFRUTE,
  DEPENDENCIA.NOMBRE,
  PERSONAL.ID_DE_EMPLEADO,
  V_FACTORESVACACIONES.FACTOR2 AS VPP,
  V_FACTORESVACACIONES.FACTOR2,
  V_FACTORESVACACIONES.FACTOR3,
  V_FACTORESVACACIONES.FACTOR7,
  V_FACTORESVACACIONES.FACTOR8,
  V_FACTORESVACACIONES.FACTOR9,
  V_FACTORESVACACIONES.FACTOR10,
  V_FACTORESVACACIONES.FACTOR11,
  V_FACTORESVACACIONES.FACTOR12,
   V_FACTORESVACACIONES.DIASRECRE,
  V_FACTORESVACACIONES.NUMPER,
  V_FACTORESVACACIONES.DIASCAL,
  V_FACTORESVACACIONES.DIASHAB    AS EXPR1,
  V_FACTORESVACACIONES.ANTIGUEDAD AS ANTIGUEDAD1,
  V_FACTORESVACACIONES.FECHA,
  V_FACTORESVACACIONES.SALVAC,
  V_FACTORESVACACIONES.VACDINERO,
  V_FACTORESVACACIONES.PRIMVAC,
  V_FACTORESVACACIONES.TOTALFACTOR,
  V_FACTORESVACACIONES.TOTALPAGO,
  V_FACTORESVACACIONES.ANTIGUEDAD,
  V_FACTORESVACACIONES.PANTIGUEDAD,
  CIUDAD.NOMBRE AS NOMBRECIUDAD,
  V_FACTORESVACACIONES.DIASPRIMA,
  V_FACTORESVACACIONES.DIASDINERO,
  V_FACTORESVACACIONES.DIASHAB,
  V_FACTORESVACACIONES.BONRECRE,
  V_FACTORESVACACIONES.DIASVAC,
  V_FACTORESVACACIONES.EXTRASPV,
  VACACIONES.COMPANIA,
  VACACIONES.ANO,
  VACACIONES.MES,
  VACACIONES.ACTO,
  VACACIONES.FECHA_ACTO
FROM V_FACTORESVACACIONES
LEFT JOIN PERSONAL
 ON V_FACTORESVACACIONES.COMPANIA       = PERSONAL.COMPANIA
AND V_FACTORESVACACIONES.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
LEFT JOIN DEPENDENCIA
 ON PERSONAL.COMPANIA    = DEPENDENCIA.COMPANIA
AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
LEFT JOIN CARGOS
ON PERSONAL.ID_DE_CARGO                          = CARGOS.ID_DE_CARGO
AND PERSONAL.COMPANIA                            = CARGOS.COMPANIA
LEFT JOIN CIUDAD
ON PERSONAL.PAIS_CED              = CIUDAD.PAIS
AND PERSONAL.DEPARTAMENTO_CED = CIUDAD.DEPARTAMENTO
AND PERSONAL.EXPEDIDA         = CIUDAD.CODIGO
LEFT JOIN VACACIONES
ON PERSONAL.ID_DE_EMPLEADO = VACACIONES.ID_DE_EMPLEADO
AND PERSONAL.COMPANIA      = VACACIONES.COMPANIA
GROUP BY PERSONAL.NOMBRECOMPLETO,
  CARGOS.NOMBRE_DEL_CARGO,
  PERSONAL.NUMERO_DCTO,
  PERSONAL.FECHA_DE_INGRESO,
  PERSONAL.FECHA_INICIO,
  PERSONAL.FECHA_FINAL,
  PERSONAL.INICIO_DISFRUTE,
  PERSONAL.FINAL_DISFRUTE,
  DEPENDENCIA.NOMBRE,
  PERSONAL.ID_DE_EMPLEADO,
  V_FACTORESVACACIONES.FACTOR2,
  V_FACTORESVACACIONES.FACTOR3,
  V_FACTORESVACACIONES.FACTOR7,
  V_FACTORESVACACIONES.FACTOR8,
  V_FACTORESVACACIONES.FACTOR9,
  V_FACTORESVACACIONES.FACTOR10,
  V_FACTORESVACACIONES.FACTOR11,
  V_FACTORESVACACIONES.FACTOR12,
  V_FACTORESVACACIONES.NUMPER,
   V_FACTORESVACACIONES.DIASRECRE,
  V_FACTORESVACACIONES.DIASCAL,
  V_FACTORESVACACIONES.ANTIGUEDAD,
  V_FACTORESVACACIONES.FECHA,
   V_FACTORESVACACIONES.SALVAC,
  V_FACTORESVACACIONES.VACDINERO,
  V_FACTORESVACACIONES.PRIMVAC,
  V_FACTORESVACACIONES.TOTALFACTOR,
  V_FACTORESVACACIONES.TOTALPAGO,
  V_FACTORESVACACIONES.PANTIGUEDAD,
  CIUDAD.NOMBRE,
  V_FACTORESVACACIONES.DIASPRIMA,
  V_FACTORESVACACIONES.DIASDINERO,
  V_FACTORESVACACIONES.DIASHAB,
  V_FACTORESVACACIONES.BONRECRE,
  V_FACTORESVACACIONES.DIASVAC,
  V_FACTORESVACACIONES.EXTRASPV,
  VACACIONES.COMPANIA,
  VACACIONES.ANO,
  VACACIONES.MES,
  VACACIONES.ACTO,
  VACACIONES.FECHA_ACTO,
  V_FACTORESVACACIONES.DIASHAB
HAVING VACACIONES.COMPANIA  = s$compania$s
AND PERSONAL.ID_DE_EMPLEADO = s$empleado$s
AND VACACIONES.ANO          = s$ano$s
AND VACACIONES.MES          = s$mes$s
ORDER BY PERSONAL.NOMBRECOMPLETO,
  CARGOS.NOMBRE_DEL_CARGO
