SELECT ANO
     , NIT_RETENEDOR
     , DV_RETENEDOR
     , RAZON_SOCIAL_RETENEDOR
     , TIPODCTO TIPO_DOCUMENTO
     , NUMERO_DOCUMENTO
     , APELLIDO1
     , APELLIDO2
     , NOMBRE1
     , NOMBRE2
     , NOMBRES
     , TO_CHAR(FECHAINICIAL,'MM') MES_INICIAL
     , TO_CHAR(FECHAINICIAL,'DD') DIA_INICIAL
     , TO_CHAR(FECHAFINAL,'MM') MES_FINAL
     , TO_CHAR(FECHAFINAL,'DD') DIA_FINAL
     , NOMBRECIUDAD
     , CODIGODEPARTAMENTO
     , CODIGOCIUDAD    
     , S37
     , S38
     , S39
     , S40
     , S41
     , (S37 + S38 + S39 + S40 + S41) S42
     , S43
     , S44
     , S45
     , S46
     , S47
     , S48 
     , VALORUVT
     , PATRIMONIOBRUTO_UVT
     , PATRIMONIOBRUTO_VALOR
     , INGRESOSTOTALES_UVT
     , INGRESOSTOTALES_VALOR
     , CONSUMOTARJETACRE_UVT
     , CONSUMOTARJETACRE_VALOR
     , COMPRASYCONSUMO_UVT
     , COMPRASYCONSUMO_VALOR
     , CONSIGNACIONESBANCARIAS_UVT
     , CONSIGNACIONESBANCARIAS_VALOR
FROM V_DIAN
WHERE COMPANIA = s$compania$s
 AND ANO       = s$anio$s
 AND NUMERO_DOCUMENTO BETWEEN 's$cedulaInicial$s' AND 's$cedulaFinal$s'
