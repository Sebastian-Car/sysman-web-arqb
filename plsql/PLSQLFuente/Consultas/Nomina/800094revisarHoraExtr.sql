SELECT HISTORICOS.COMPANIA,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  SUM(HISTORICOS.VALOR)     AS TOTAL,
  SUM(HISTORICOS.VALOR) AS HORAS,
  PERSONAL.ID_DE_EMPLEADO,
  PERSONAL.APELLIDO1,
  PERSONAL.APELLIDO2,
  PERSONAL.NOMBRES
FROM HISTORICOS
LEFT JOIN PERSONAL
ON HISTORICOS.COMPANIA        = PERSONAL.COMPANIA
AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
WHERE HISTORICOS.COMPANIA = s$compania$s
AND HISTORICOS.ID_DE_CONCEPTO BETWEEN '017' AND '027'
AND HISTORICOS.ID_DE_PROCESO = s$procesoSesion$s
AND HISTORICOS.ANO           =s$anioSesion$s
AND HISTORICOS.PERIODO =s$periodoSesion$s
AND HISTORICOS.MES  = s$mesSesion$s
GROUP BY HISTORICOS.COMPANIA,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  PERSONAL.ID_DE_EMPLEADO,
  PERSONAL.APELLIDO1,
  PERSONAL.APELLIDO2,
  PERSONAL.NOMBRES
ORDER BY SUM(HISTORICOS.VALOR) DESC
