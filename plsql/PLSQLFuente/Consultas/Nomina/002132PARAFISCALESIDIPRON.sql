SELECT HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  MES.NOMBRE AS NOMBRE_MES,
  PERSONAL.ID_CENTRO_DE_COSTO,
  CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO,
  CONCEPTOS.NOMBRE_CONCEPTO,
  SUM(HISTORICOS.VALOR) AS SUMADEVALOR,
  HISTORICOS.ID_DE_PROCESO,
  DEPENDENCIA.CODIGO AS DEPENDENCIA,
  DEPENDENCIA.NOMBRE,
  HISTORICOS.ID_DE_CONCEPTO
FROM HISTORICOS
INNER JOIN PERSONAL
ON PERSONAL.COMPANIA = HISTORICOS.COMPANIA
AND PERSONAL.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
INNER JOIN CENTRO_COSTO
ON CENTRO_COSTO.COMPANIA = HISTORICOS.COMPANIA
AND CENTRO_COSTO.ANO = HISTORICOS.ANO
AND CENTRO_COSTO.CODIGO = PERSONAL.ID_CENTRO_DE_COSTO
INNER JOIN DEPENDENCIA
ON DEPENDENCIA.COMPANIA = HISTORICOS.COMPANIA
AND DEPENDENCIA.CODIGO = PERSONAL.DEPENDENCIA
INNER JOIN CONCEPTOS
ON CONCEPTOS.COMPANIA = HISTORICOS.COMPANIA
AND CONCEPTOS.ID_DE_CONCEPTO = HISTORICOS.ID_DE_CONCEPTO
INNER JOIN MES 
ON MES.COMPANIA = HISTORICOS.COMPANIA
AND MES.ANO = HISTORICOS.ANO
AND MES.NUMERO = HISTORICOS.MES
WHERE  HISTORICOS.COMPANIA          = s$compania$s
     AND HISTORICOS.ANO             = s$anio$s     
     AND HISTORICOS.MES             = s$mes$s
     AND HISTORICOS.PERIODO         = s$periodo$s
     AND HISTORICOS.ID_DE_PROCESO   = s$proceso$s
     AND HISTORICOS.ID_DE_CONCEPTO BETWEEN '101' AND '103'
GROUP BY HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  MES.NOMBRE,
  PERSONAL.ID_CENTRO_DE_COSTO,
  CENTRO_COSTO.NOMBRE,
  CONCEPTOS.NOMBRE_CONCEPTO,
  HISTORICOS.ID_DE_PROCESO,
  DEPENDENCIA.CODIGO,
  DEPENDENCIA.NOMBRE,
  HISTORICOS.ID_DE_CONCEPTO