SELECT BANCOS_NOMINA.BANCO,
  BANCOS_NOMINA.NOMBRE,
  BANCOS_NOMINA.TELEFONO,
  BANCOS_NOMINA.FAX,
  CASE
    WHEN PERSONAL.TIPOCUENTA IN ('A')
    THEN 'Ahorros'
    WHEN PERSONAL.TIPOCUENTA IN ('C')
    THEN 'Corriente'
    ELSE ''
  END TIPOCUENTA,
  PERSONAL.NUMERO_DCTO,
  PERSONAL.NOMBRECOMPLETO AS NOMBRECOMPLETO,
  PERSONAL.CUENTA,
  HISTORICOS.VALOR SUMADEVALOR
FROM PERSONAL
INNER JOIN BANCOS_NOMINA
ON PERSONAL.COMPANIA = BANCOS_NOMINA.COMPANIA
AND PERSONAL.BANCO   = BANCOS_NOMINA.BANCO
INNER JOIN HISTORICOS
ON PERSONAL.COMPANIA           = HISTORICOS.COMPANIA
AND PERSONAL.ID_DE_EMPLEADO    = HISTORICOS.ID_DE_EMPLEADO
WHERE PERSONAL.COMPANIA        = 's$compania$s'
AND HISTORICOS.ID_DE_PROCESO   = s$proceso$s
AND HISTORICOS.ANO             = s$anio$s
AND HISTORICOS.MES             = s$mes$s
AND HISTORICOS.PERIODO         = s$periodo$s
AND HISTORICOS.ID_DE_CONCEPTO IN (144)
ORDER BY PERSONAL.CUENTA