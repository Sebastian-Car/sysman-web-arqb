SELECT V_ACUMULADOS.ID_DE_EMPLEADO  CODIGO,
      V_ACUMULADOS.PERIODO , 
      V_ACUMULADOS.ANO,
      V_ACUMULADOS.MES,
      V_ACUMULADOS.ID_DE_PROCESO PROCESO,
      PERSONAL_HISTORICO.ID_DE_CARGO CARGO, 
      PERSONAL_HISTORICO.NOMBRE_DE_CARGO NOMBRECARGO, 
      PERSONAL_HISTORICO.DEPENDENCIA DEPENDENCIA, 
      PERSONAL_HISTORICO.NOMBRE_DEPENDENCIA NOMBREDEPENDENCIA, 
      PERSONAL_HISTORICO.NOMBRECOMPLETO NOM_EMPLEADO,
      PERSONAL_HISTORICO.NUMERO_DCTO CEDULA,   
      SUM(V_ACUMULADOS.DEVENGOS) DEVENGOS,
      SUM(V_ACUMULADOS.DESCUENTOS) DESCUENTOS,
      SUM(V_ACUMULADOS.NETOAPAGAR) NETO,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=9  THEN V_ACUMULADOS.VALOR ELSE 0 END) C009,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=16 THEN V_ACUMULADOS.VALOR ELSE 0 END) C016,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=24 THEN V_ACUMULADOS.VALOR ELSE 0 END) C024,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=18 THEN V_ACUMULADOS.VALOR ELSE 0 END) C018,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=20 THEN V_ACUMULADOS.VALOR ELSE 0 END) C020,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=21 THEN V_ACUMULADOS.VALOR ELSE 0 END) C021,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=38 THEN V_ACUMULADOS.VALOR ELSE 0 END) C038,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=33 THEN V_ACUMULADOS.VALOR ELSE 0 END) c033,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=32 THEN V_ACUMULADOS.VALOR ELSE 0 END) C032,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=2  THEN V_ACUMULADOS.VALOR ELSE 0 END) C002,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=50 THEN V_ACUMULADOS.VALOR ELSE 0 END) C050,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=51 THEN V_ACUMULADOS.VALOR ELSE 0 END) C051,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=56 THEN V_ACUMULADOS.VALOR ELSE 0 END) C056,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=52 THEN V_ACUMULADOS.VALOR ELSE 0 END) C052,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=53 THEN V_ACUMULADOS.VALOR ELSE 0 END) c053,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=54 THEN V_ACUMULADOS.VALOR ELSE 0 END) c054,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=57 THEN V_ACUMULADOS.VALOR ELSE 0 END) C057,
      SUM(CASE WHEN V_ACUMULADOS.ID_DE_CONCEPTO=70 THEN V_ACUMULADOS.VALOR ELSE 0 END) C070
FROM V_ACUMULADOS INNER JOIN PERSONAL_HISTORICO
  ON V_ACUMULADOS.COMPANIA        = PERSONAL_HISTORICO.COMPANIA
 AND V_ACUMULADOS.ID_DE_PROCESO   = PERSONAL_HISTORICO.ID_DE_PROCESO
 AND V_ACUMULADOS.ANO             = PERSONAL_HISTORICO.ANO
 AND V_ACUMULADOS.MES             = PERSONAL_HISTORICO.MES
 AND V_ACUMULADOS.PERIODO         = PERSONAL_HISTORICO.PERIODO
 AND V_ACUMULADOS.ID_DE_EMPLEADO  = PERSONAL_HISTORICO.ID_DE_EMPLEADO
WHERE V_ACUMULADOS.COMPANIA       = s$compania$s
  AND V_ACUMULADOS.ID_DE_PROCESO  = s$procesoNomina$s
  AND V_ACUMULADOS.ANO            = s$anioNomina$s
  AND V_ACUMULADOS.MES            = s$mesNomina$s
  AND V_ACUMULADOS.PERIODO        = s$periodoNomina$s
  AND PERSONAL_HISTORICO.ESTADO_ACTUAL <>3  
GROUP BY V_ACUMULADOS.ID_DE_EMPLEADO,
      V_ACUMULADOS.ANO,
      V_ACUMULADOS.ID_DE_PROCESO,
      V_ACUMULADOS.MES,
      V_ACUMULADOS.PERIODO,
      PERSONAL_HISTORICO.ID_DE_CARGO , 
      PERSONAL_HISTORICO.NOMBRE_DE_CARGO , 
      PERSONAL_HISTORICO.DEPENDENCIA , 
      PERSONAL_HISTORICO.NOMBRE_DEPENDENCIA , 
      PERSONAL_HISTORICO.NOMBRECOMPLETO , 
      PERSONAL_HISTORICO.NUMERO_DCTO
