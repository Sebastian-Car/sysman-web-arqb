SELECT MIN(V_ACUMULADOS.ID_DE_EMPLEADO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) ID_DE_EMPLEADO, 
       V_ACUMULADOS.ID_DE_CONCEPTO, PERIODO,
       V_ACUMULADOS.ANO,
       V_ACUMULADOS.MES,
       MIN(V_ACUMULADOS.NOMBRE_CONCEPTO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) NOMBRE_CONCEPTO, 
       MIN(V_ACUMULADOS.NOMBRECOMPLETO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) NOMBRECOMPLETO, 
       MIN(V_ACUMULADOS.NUMERO_DCTO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) NUMERO_DCTO,                  
       V_ACUMULADOS.CLASE,    
       V_ACUMULADOS.CLASE_NOMBRE AS NOMBRECLASE, 
       SUM(V_ACUMULADOS.VALOR) SUMA_DE_VALOR,    
       SUM(V_ACUMULADOS.CANTIDAD) CANTIDAD,    
       SUM(V_ACUMULADOS.NETOAPAGAR) NETOAPAGAR
FROM V_ACUMULADOS    
WHERE V_ACUMULADOS.COMPANIA = s$compania$s
 AND V_ACUMULADOS.ANO || LPAD(V_ACUMULADOS.MES,2,0) || LPAD(V_ACUMULADOS.PERIODO,2,0) BETWEEN s$ano1$s || s$mes1$s || s$periodo1$s AND s$ano2$s || s$mes2$s || s$periodo3$s 
 AND V_ACUMULADOS.ID_DE_EMPLEADO = s$empleado$s
 AND V_ACUMULADOS.CLASE IN(3, 5, 8, 99, 4, 6, 7)
GROUP BY  V_ACUMULADOS.ID_DE_CONCEPTO, 
        V_ACUMULADOS.CLASE,    
         V_ACUMULADOS.CLASE_NOMBRE,
         PERIODO,ANO,MES
