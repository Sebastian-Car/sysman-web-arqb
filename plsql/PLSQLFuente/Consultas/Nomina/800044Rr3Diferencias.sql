WITH RR1 AS
(
  SELECT  HISTORICOS.COMPANIA, 
        HISTORICOS.ID_DE_PROCESO, 
        HISTORICOS.ANO, 
        HISTORICOS.MES, 
        HISTORICOS.ID_DE_EMPLEADO, 
        SUM(HISTORICOS.VALOR) SUMADEVALOR, 
        SUM(HISTORICOS.VALOR*CATEGORIA.VLR_INCREMENTO/100) RETR, 
        CATEGORIA.VLR_INCREMENTO, 
        CONCEPTOS.C_RETRO
      FROM ((HISTORICOS 
          LEFT JOIN CONCEPTOS 
             ON HISTORICOS.ID_DE_CONCEPTO   = CONCEPTOS.ID_DE_CONCEPTO 
            AND HISTORICOS.COMPANIA              = CONCEPTOS.COMPANIA) 
          LEFT JOIN PERSONAL 
             ON HISTORICOS.ID_DE_EMPLEADO   = PERSONAL.ID_DE_EMPLEADO 
            AND HISTORICOS.COMPANIA              = PERSONAL.COMPANIA) 
          LEFT JOIN CATEGORIA 
             ON PERSONAL.ANO            	           = CATEGORIA.ANO
            AND PERSONAL.ID_DE_CATEGORIA    = CATEGORIA.ID_DE_CATEGORIA
            AND PERSONAL.ESCALAFON                = CATEGORIA.ESCALAFON
            AND PERSONAL.COMPANIA                  = CATEGORIA.COMPANIA
      WHERE HISTORICOS.COMPANIA   	=  s$compania$s
        AND HISTORICOS.ANO        	=  s$anioNomina$s 
        AND HISTORICOS.MES        	=  s$mesNomina$s 
        AND HISTORICOS.PERIODO    	NOT IN(05)
        AND CATEGORIA.ANO         	=  PERSONAL.ANO         
        AND CONCEPTOS.C_RETRO     	=  s$rr2$s
      GROUP BY  HISTORICOS.COMPANIA, 
            HISTORICOS.ID_DE_PROCESO, 
            HISTORICOS.ANO, 
            HISTORICOS.MES, 
            HISTORICOS.ID_DE_EMPLEADO, 
            CATEGORIA.VLR_INCREMENTO, 
            CONCEPTOS.C_RETRO
    ORDER BY  HISTORICOS.COMPANIA, 
          HISTORICOS.ID_DE_PROCESO, 
          HISTORICOS.ANO, 
          HISTORICOS.MES, 
          HISTORICOS.ID_DE_EMPLEADO
),
RR2 AS
(
  SELECT  HISTORICOS.COMPANIA, 
          HISTORICOS.ID_DE_PROCESO, 
          HISTORICOS.ANO, 
          HISTORICOS.MES, 
          HISTORICOS.ID_DE_EMPLEADO, 
          SUM(HISTORICOS.VALOR) VALOR
    FROM HISTORICOS 
      LEFT JOIN CONCEPTOS 
         ON HISTORICOS.ID_DE_CONCEPTO   = CONCEPTOS.ID_DE_CONCEPTO
        AND HISTORICOS.COMPANIA              = CONCEPTOS.COMPANIA
    WHERE HISTORICOS.COMPANIA             = s$compania$s 
      AND HISTORICOS.ANO              	      = s$anioNomina$s
      AND HISTORICOS.MES              	      = s$mesNomina$s
      AND HISTORICOS.PERIODO                  = 05 
      AND HISTORICOS.ID_DE_CONCEPTO   = s$rr2$s       
    GROUP BY  HISTORICOS.COMPANIA, 
          HISTORICOS.ID_DE_PROCESO, 
          HISTORICOS.ANO, 
          HISTORICOS.MES, 
          HISTORICOS.ID_DE_EMPLEADO
    ORDER BY  HISTORICOS.COMPANIA, 
          HISTORICOS.ID_DE_PROCESO, 
          HISTORICOS.ANO, 
          HISTORICOS.ID_DE_EMPLEADO
)
SELECT  RR1.COMPANIA, 
    RR1.ID_DE_PROCESO, 
    RR1.ANO, RR1.MES, 
    RR1.ID_DE_EMPLEADO, 
    RR1.SUMADEVALOR, 
    RR1.RETR, 
    RR2.VALOR, 
    (RR1.RETR - RR2.VALOR)  DIF, 
    RR1.VLR_INCREMENTO
FROM RR1 
    LEFT JOIN RR2 
       ON RR1.ID_DE_EMPLEADO   = RR2.ID_DE_EMPLEADO
      AND RR1.MES             	           = RR2.MES
      AND RR1.ANO                        = RR2.ANO
      AND RR1.ID_DE_PROCESO   = RR2.ID_DE_PROCESO
      AND RR1.COMPANIA             = RR2.COMPANIA
ORDER BY RR1.RETR - RR2.VALOR DESC
