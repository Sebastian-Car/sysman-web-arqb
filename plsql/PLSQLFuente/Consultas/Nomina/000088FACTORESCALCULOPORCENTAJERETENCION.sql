SELECT CONCEPTOS.ID_DE_CONCEPTO,
       CONCEPTOS.NOMBRE_CONCEPTO,
       CASE WHEN CONCEPTOS.DEVRETENCION NOT IN(0) THEN 'x' ELSE ' ' END DEVRETENCION,
       CASE WHEN CONCEPTOS.DEDRETENCION NOT IN(0) THEN 'x' ELSE ' ' END DEDRETENCION
FROM CONCEPTOS 
LEFT JOIN COMPANIA ON CONCEPTOS.COMPANIA = COMPANIA.CODIGO
WHERE CONCEPTOS.COMPANIA          =  s$compania$s
  AND (CONCEPTOS.DEVRETENCION     <> 0 
   OR  CONCEPTOS.DEDRETENCION     <> 0)
ORDER BY CONCEPTOS.DEVRETENCION,
         CONCEPTOS.DEDRETENCION,
         CONCEPTOS.ID_DE_CONCEPTO
