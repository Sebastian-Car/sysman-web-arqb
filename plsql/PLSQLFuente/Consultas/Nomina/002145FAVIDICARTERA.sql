MERGE INTO CONSULTAS FIN USING (SELECT '002145FAVIDICARTERA' INFORME ,TO_CLOB(q'[SELECT 
	CENTRO_COSTO.NOMBRE AS NOMBRE_CENTRO_DE_COSTO, 
	SUBSTR(CENTRO_COSTO.CODIGO,2) AS TIPOCENTRO,
	PERSONAL.DEPENDENCIA AS DEPENDENCIA,
	DEPENDENCIA.NOMBRE,
	HISTORICOS.ANO,
	TO_CHAR(HISTORICOS.FECHA,'MONTH','nls_date_language=spanish') AS MES,
    SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '972' THEN HISTORICOS.VALOR ELSE 0 END) AS BASE_LIQUIDACION_CESANTIAS, 
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '440' THEN HISTORICOS.VALOR ELSE 0 END) AS APORTES_CESANTIAS, 
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '441' THEN HISTORICOS.VALOR ELSE 0 END) AS COMISION_ADMIN_CESANTIAS, 
	SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '440' THEN HISTORICOS.VALOR ELSE 0 END) + 
		SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '441' THEN HISTORICOS.VALOR ELSE 0 END) AS SUM_APORTES,
	CENTRO_COSTO.CODIGO AS ID_CENTRO_DE_COSTO

FROM 
	HISTORICOS
	INNER JOIN CONCEPTOS
		ON HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA
		AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
	INNER JOIN PERSONAL
		ON HISTORICOS.COMPANIA = PERSONAL.COMPANIA
		AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
	INNER JOIN DEPENDENCIA
		ON PERSONAL.COMPANIA = DEPENDENCIA.COMPANIA
		AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
	INNER JOIN CENTRO_COSTO
		ON PERSONAL.ANO = CENTRO_COSTO.ANO
		AND PERSONAL.COMPANIA = CENTRO_COSTO.COMPANIA
		AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
	-- INNER JOIN CESANTIAS
WHERE
	HISTORICOS.COMPANIA = s$compania$s
	AND HISTORICOS.ID_DE_PROCESO = s$proceso$s
	AND HISTORICOS.ANO = s$ano$s
	AND HISTORICOS.MES = s$mes$s
	AND HISTORICOS.PERIODO = s$periodo$s
	AND PERSONAL.FONDO_CESANTIAS = 'CES286' 
	--AND PERSONAL.ESTADO_ACTUAL = 1
GROUP BY 
	CENTRO_COSTO.NOMBRE, 
	CENTRO_COSTO.CODIGO,
	PERSONAL.DEPENDENCIA,
	DEPENDENCIA.NOMBRE,
	HISTORICOS.ANO,
	HISTORICOS.FECHA,
	PERSONAL.ID_CENTRO_DE_COSTO
HAVING SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '972' THEN HISTORICOS.VALOR ELSE 0 END) != 0
		OR SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '440' THEN HISTORICOS.VALOR ELSE 0 END) != 0 
		OR SUM( CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '441' THEN HISTORICOS.VALOR ELSE 0 END) != 0
ORDER BY 
	SUBSTR(CENTRO_COSTO.CODIGO,2),
	DEPENDENCIA.NOMBRE]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);