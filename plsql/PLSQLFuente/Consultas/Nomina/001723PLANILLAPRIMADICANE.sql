SELECT ESTABLECIMIENTOS_DOCENTES.CODIGO ESTABLECIMIENTO,
  ESTABLECIMIENTOS_DOCENTES.NOMBRE NOMBREESTABLECIMIENTO,
  PERSONAL.ID_DE_EMPLEADO,
  PERSONAL.APELLIDO1
  || ' '
  || PERSONAL.APELLIDO2
  || ' '
  || PERSONAL.NOMBRES NOMCOMPLETO,
  PERSONAL.NUMERO_DCTO,
  PERSONAL.FECHA_DE_INGRESO,
  CENTRO_COSTO.NOMBRE NOMBRE_CENTRO_DE_COSTO,
  CARGOS.NOMBRE_DEL_CARGO,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  CENTRO_COSTO.CODIGO ID_CENTRO_DE_COSTO,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 158
    THEN HISTORICOS.VALOR
    ELSE 0
  END) PRIMADIC,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 930
    THEN HISTORICOS.VALOR
    ELSE 0
  END) SUELDOPRIMADIC,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 159
    THEN HISTORICOS.VALOR
    ELSE 0
  END) + 
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 160
    THEN HISTORICOS.VALOR
    ELSE 0
  END) PRIMAEXTRASEMESTRAL,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=931
    THEN HISTORICOS.VALOR
    ELSE 0
  END) PRIMASEMESTRAL,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=932
    THEN HISTORICOS.VALOR
    ELSE 0
  END) PRIMAVAC,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=933
    THEN HISTORICOS.VALOR
    ELSE 0
  END) TRANSPRIMADIC,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=934
    THEN HISTORICOS.VALOR
    ELSE 0
  END) ALIMENPRIMADIC,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=935
    THEN HISTORICOS.VALOR
    ELSE 0
  END) GASTOSREP,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=939
    THEN HISTORICOS.VALOR
    ELSE 0
  END) BON,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=940
    THEN HISTORICOS.VALOR
    ELSE 0
  END) VPT,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=936
    THEN HISTORICOS.VALOR
    ELSE 0
  END) DIASPRIMADIC,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=937
    THEN HISTORICOS.VALOR
    ELSE 0
  END) DIASCOMPRIMADIC,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=938
    THEN HISTORICOS.VALOR
    ELSE 0
  END) DIASLICPRIMADIC,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=942
    THEN HISTORICOS.VALOR
    ELSE 0
  END) EXTRAS,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=140
    THEN HISTORICOS.VALOR
    ELSE 0
  END) DESCUENTOS,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=144
    THEN HISTORICOS.VALOR
    ELSE 0
  END) NETO,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=150
    THEN HISTORICOS.VALOR
    ELSE 0
  END) BASP,
  SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO=170
    THEN HISTORICOS.VALOR
    ELSE 0
  END) PANTIG
FROM HISTORICOS
  INNER JOIN PERSONAL
    ON HISTORICOS.COMPANIA        = PERSONAL.COMPANIA
    AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
  INNER JOIN DEPENDENCIA
    ON PERSONAL.COMPANIA     = DEPENDENCIA.COMPANIA
    AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
  INNER JOIN CARGOS
    ON PERSONAL.COMPANIA     = CARGOS.COMPANIA
    AND PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO
  INNER JOIN ESTABLECIMIENTOS_DOCENTES
    ON PERSONAL.COMPANIA                = ESTABLECIMIENTOS_DOCENTES.COMPANIA
    AND PERSONAL.CODIGO_ESTABLECIMIENTO = ESTABLECIMIENTOS_DOCENTES.CODIGO
  INNER JOIN CENTRO_COSTO
    ON PERSONAL.COMPANIA            = CENTRO_COSTO.COMPANIA
    AND PERSONAL.ANO                = CENTRO_COSTO.ANO
    AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
WHERE HISTORICOS.COMPANIA       = s$compania$s
  AND HISTORICOS.ID_DE_CONCEPTO IN (158,930,931,932,933,934,935,936,937,938,939,940,942,140,144,159,150,140,097,163,170, 160)
  AND HISTORICOS.ID_DE_PROCESO  = s$proceso$s
  AND HISTORICOS.ANO             = s$anio$s
  AND HISTORICOS.MES             = s$mes$s
  AND HISTORICOS.PERIODO         = s$periodo$s
GROUP BY ESTABLECIMIENTOS_DOCENTES.CODIGO,
  ESTABLECIMIENTOS_DOCENTES.NOMBRE,
  PERSONAL.ID_DE_EMPLEADO,
  DEPENDENCIA.CODIGO,
  PERSONAL.APELLIDO1
  || ' '
  || PERSONAL.APELLIDO2
  || ' '
  || PERSONAL.NOMBRES,
  PERSONAL.NUMERO_DCTO,
  PERSONAL.FECHA_DE_INGRESO,
  CENTRO_COSTO.NOMBRE,
  CARGOS.NOMBRE_DEL_CARGO,
  HISTORICOS.COMPANIA,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  CENTRO_COSTO.CODIGO,
  PERSONAL.ESTADO_ACTUAL
HAVING SUM(
  CASE
    WHEN HISTORICOS.ID_DE_CONCEPTO = 144
    THEN HISTORICOS.VALOR
    ELSE 0
  END) > 0
ORDER BY PERSONAL.APELLIDO1
  || ' '
  || PERSONAL.APELLIDO2
  || ' '
  || PERSONAL.NOMBRES
