SELECT DISTINCT 
  PERSONAL.APELLIDO1 ||' '||  PERSONAL.APELLIDO2 ||' '|| PERSONAL.NOMBRES NOMBRECOMP, 
  PERSONAL.NUMERO_DCTO,  
  PERSONAL.FECHA_DE_INGRESO, 
  CATEGORIA.SALARIO_BASE,  
  CARGOS.NOMBRE_DEL_CARGO,  
  DEPENDENCIA.NOMBRE,  
  CENTRO_COSTO.NOMBRE NOMBRE_CENTRO_DE_COSTO, 
  TO_CHAR(SYSDATE, 'YYYY') - TO_CHAR(PERSONAL.FECHA_DE_INGRESO, 'YYYY')  DIFANO, 
  PERSONAL.DIASINTERRUPCION, 
   TO_CHAR(TO_DATE(TO_CHAR(FECHA_DE_INGRESO, 'DD')||  TO_CHAR(FECHA_DE_INGRESO, 'MM') ||(TO_CHAR(SYSDATE, 'YYYY'))) + NVL(DIASINTERRUPCION, 0) -1, 'DD/MM/YYYY') FECHA 
FROM PERSONAL  
	LEFT JOIN DEPENDENCIA  
	ON PERSONAL.COMPANIA = DEPENDENCIA.COMPANIA   
	AND PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO  
LEFT JOIN CARGOS  
ON PERSONAL.COMPANIA = CARGOS.COMPANIA 
AND PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO  
LEFT JOIN CENTRO_COSTO  
ON PERSONAL.COMPANIA = CENTRO_COSTO.COMPANIA  
AND PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO  
LEFT JOIN CATEGORIA  
ON PERSONAL.COMPANIA = CATEGORIA.COMPANIA 
AND PERSONAL.ESCALAFON = CATEGORIA.ESCALAFON  
AND PERSONAL.ID_DE_CATEGORIA = CATEGORIA.ID_DE_CATEGORIA  
WHERE PERSONAL.COMPANIA =  s$compania$s 
  AND (TO_CHAR(SYSDATE, 'YYYY') -  TO_CHAR(PERSONAL.FECHA_DE_INGRESO, 'YYYY')) IN ( 5, 10, 15, 20, 25, 30)  
  AND ID_DE_EMPLEADO <> 0 
  AND TO_CHAR(FECHA_DE_INGRESO, 'MM') = s$mes$s         
  AND ESTADO_ACTUAL = 1 
  AND CATEGORIA.ANO = TO_CHAR(SYSDATE, 'YYYY') 
  AND TO_CHAR(PERSONAL.FECHA_DE_INGRESO, 'YYYY') <> TO_CHAR(SYSDATE, 'YYYY') 
ORDER BY PERSONAL.APELLIDO1 ||' '||  PERSONAL.APELLIDO2 ||' '|| PERSONAL.NOMBRES,  
 TO_CHAR(SYSDATE, 'YYYY') - TO_CHAR(PERSONAL.FECHA_DE_INGRESO, 'YYYY')
