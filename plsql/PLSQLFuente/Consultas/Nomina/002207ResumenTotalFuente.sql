MERGE INTO CONSULTAS FIN USING (SELECT '002207ResumenTotalFuente' INFORME ,TO_CLOB(q'[SELECT DISTINCT
HISTORICOS.COMPANIA,
PERSONAL.CODIGO_ESTABLECIMIENTO ESTABLECIMIENTO,
ESTABLECIMIENTOS_DOCENTES.NOMBRE NOM_ESTABLECIMIENTO,
HISTORICOS.ID_DE_CONCEPTO,
CONCEPTOS.NOMBRE_CONCEPTO,
CONCEPTOS.CLASE,
PCK_SYSMAN_UTL.FC_NOMBRE_MES(HISTORICOS.MES)  NOMBRE_MES,
SUM(HISTORICOS.VALOR) AS SUMA_DE_VALOR,
SUM(CASE WHEN CONCEPTOS.CLASE IN (3,8) THEN HISTORICOS.VALOR ELSE (CASE WHEN CONCEPTOS.CLASE = 5 THEN (HISTORICOS.VALOR *-1) ELSE 0 END) END)  AS TOT, 
CONCEPTOS.RELACIONADO,
PERIODOS.NOM_PERIODO NOMBRE_PERIODO,
CASE CONCEPTOS.CLASE 
WHEN 3 THEN 'VALOR DEVENGOS'
WHEN 5 THEN 'VALOR DESCUENTOS' 
WHEN 4 THEN 'VALOR TOTAL DEVENGOS' 
WHEN 6 THEN 'VALOR TOTAL  DESCUENTOS' 
WHEN 7 THEN 'VALOR NETO A PAGAR' 
ELSE 'CONCEPTOS CONTABLES' END AS CLASECONCEPTO,
HISTORICOS.ID_DE_PROCESO,
HISTORICOS.ANO,
HISTORICOS.MES,
HISTORICOS.PERIODO
FROM HISTORICOS 
LEFT JOIN CONCEPTOS 
ON HISTORICOS.COMPANIA = CONCEPTOS.COMPANIA
AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
LEFT JOIN COMPANIA 
ON HISTORICOS.COMPANIA = COMPANIA.CODIGO
LEFT JOIN PERSONAL
ON PERSONAL.COMPANIA = HISTORICOS.COMPANIA
AND PERSONAL.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO
LEFT JOIN PERIODOS 
ON HISTORICOS.COMPANIA = PERIODOS.COMPANIA
AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO 
AND HISTORICOS.ANO = PERIODOS.ANO 
AND HISTORICOS.MES = PERIODOS.MES 
AND HISTORICOS.PERIODO = PERIODOS.PERIODO
LEFT JOIN ESTABLECIMIENTOS_DOCENTES
ON HISTORICOS.COMPANIA = ESTABLECIMIENTOS_DOCENTES.COMPANIA
AND PERSONAL.CODIGO_ESTABLECIMIENTO = ESTABLECIMIENTOS_DOCENTES.CODIGO
WHERE 
HISTORICOS.COMPANIA = s$compania$s
AND CONCEPTOS.CLASE IN (3,4,5,6,7,8)
AND PERSONAL.CODIGO_ESTABLECIMIENTO  = (CASE WHEN 'TODAS' = 'TODAS' THEN PERSONAL.CODIGO_ESTABLECIMIENTO ELSE  'TODAS' END)
AND HISTORICOS.ID_DE_PROCESO  = s$procesoNomina$s
AND HISTORICOS.ANO BETWEEN s$anioNomina$s AND s$anioNomina$s
AND HISTORICOS.MES BETWEEN s$mesNomina$s AND s$mesNomina$s
AND HISTORICOS.PERIODO BETWEEN s$periodoNomina$s AND s$periodoNomina$s

GROUP BY
HISTORICOS.COMPANIA,
CODIGO_ESTABLECIMIENTO,
HISTORICOS.ID_DE_CONCEPTO, CONCEPTOS.NOMBRE_CONCEPTO, CONCEPTOS.CLASE, 
CONCEPTOS.RELACIONADO, CASE CONCEPTOS.CLASE WHEN 3 THEN 'VALOR DEVENGOS' WHEN 5 THEN 'VALOR DESCUENTOS' WHEN 4 THEN 'VALOR TOTAL DEVENGOS' WHEN 6 THEN 'VALOR TOTAL  DESCUENTOS' WHEN 7 THEN 'VALOR NETO A PAGAR' ELSE 'CONCEPTOS CONTABLES' END, HISTORICOS.ID_DE_PROCESO, HISTORICOS.ANO, HISTORICOS.MES, 
HISTORICOS.PERIODO
, PERIODOS.NOM_PERIODO
,
ESTABLECIMIENTOS_DOCENTES.NOMBRE

HAVING SUM(HISTORICOS.VALOR)<>0
ORDER BY HISTORICOS.ID_DE_CONCEPTO]') CONSULTA, 6 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);