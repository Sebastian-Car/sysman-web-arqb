SELECT V_PERSONAL.NUMERO_DCTO,
  V_PERSONAL.APELLIDO1
  ||' '
  || V_PERSONAL.APELLIDO2
  ||' '
  || V_PERSONAL.NOMBRES NOMCOMPLETO,
  V_PERSONAL.FECHA_DE_INGRESO,
  V_PERSONAL.FECHA_DE_RETIRO,
  CASE
    WHEN V_PERSONAL.SALARIO_BASE_CATE IN(0)
    THEN V_PERSONAL.MESADA_PENSIONAL
    ELSE V_PERSONAL.SALARIO_BASE_CATE
  END SALARIO_BASE,
  V_PERSONAL.FECHANCTO,
  V_PERSONAL.SEXO,
  PCK_SYSMAN_UTL.FC_EDAD(UN_FECHA => V_PERSONAL.FECHANCTO) EDAD,
  PCK_SYSMAN_UTL.FC_EDAD(UN_FECHAINI => V_PERSONAL.FECHA_DE_INGRESO, UN_FECHAFIN => SYSDATE,UN_FORMATO => 1,UN_EDADPERSONA=>0) TSERV,
  V_PERSONAL.APELLIDO1CAUSANTE
  ||' '
  || V_PERSONAL.APELLIDO2CAUSANTE
  ||' '
  || V_PERSONAL.NOMBRE1CAUSANTE
  ||' '
  ||V_PERSONAL.NOMBRE2CAUSANTE CAUSANTE,
  V_PERSONAL.NUMERO_DCTOCAUSANTE,
  TIPO_PENSIONADO.NOMBRE TIPO_PENSIONADO
FROM V_PERSONAL
  INNER JOIN TIPO_PENSIONADO
     ON V_PERSONAL.TIPOPENSIONADO = TIPO_PENSIONADO.CODIGO
WHERE V_PERSONAL.COMPANIA         = s$compania$s
  AND V_PERSONAL.ID_DE_PROCESO    = s$procesoNomina$s
  AND V_PERSONAL.ANO              = s$anoNomina$s
  AND V_PERSONAL.MES              = s$mesNomina$s
  AND V_PERSONAL.PERIODO          = s$periodoNomina$s
  AND V_PERSONAL.FECHA_DE_INGRESO BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
  AND V_PERSONAL.FECHA_DE_RETIRO  IS NULL
  AND V_PERSONAL.ESTADO_ACTUAL    IN ('2')
