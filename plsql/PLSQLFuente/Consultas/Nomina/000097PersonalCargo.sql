SELECT DISTINCT 
         PERSONAL.ID_DE_EMPLEADO, 
         PERSONAL.NUMERO_DCTO, 
         PERSONAL.FECHA_DE_INGRESO,
         PERSONAL.NOMBRECOMPLETO NombreCompleto,
         CARGOS.NOMBRE_DEL_CARGO,
         CASE WHEN PERSONAL.CARGO_ENCARGO IS NOT NULL
         AND ENCARGOS.FECHAINICIO <= PERIODOS.FECHAINICIO
         AND ENCARGOS.FECHAFINAL >= PERIODOS.FECHAFINAL
         THEN  PERSONAL.CARGO_ENCARGO
         ELSE PERSONAL.ID_DE_CARGO
         END ID_DE_CARGO,
         PERSONAL.ESTADO_ACTUAL, 
         PERSONAL.COMPANIA, 
         DEPENDENCIA.NOMBRE NOMBREDEPENDENCIA,
         ENCARGOS.FECHAINICIO,
         ENCARGOS.FECHAFINAL,
         PERIODOS.FECHAINICIO PFI,
         PERIODOS.FECHAFINAL PFF,
         PERIODOS.ID_DE_PROCESO,
         PERIODOS.MES,
         PERIODOS.PERIODO

  FROM PERSONAL
  LEFT JOIN PERIODOS
    ON PERSONAL.COMPANIA = PERIODOS.COMPANIA
    AND PERSONAL.ANO = PERIODOS.ANO
    AND PERIODOS.ID_DE_PROCESO =  s$proceso$s
    AND PERIODOS.MES =  s$mes$s
    AND PERIODOS.PERIODO =  s$periodo$s
  LEFT JOIN ENCARGOS
    ON PERSONAL.COMPANIA = ENCARGOS.COMPANIA
    AND PERSONAL.ID_DE_EMPLEADO = ENCARGOS.ID_DE_EMPLEADO
    AND PERSONAL.ANO = ENCARGOS.ANO
    AND ENCARGOS.FECHAINICIO <= PERIODOS.FECHAINICIO
    AND ENCARGOS.FECHAFINAL >= PERIODOS.FECHAFINAL
    
  LEFT JOIN CARGOS 
    ON PERSONAL.COMPANIA = CARGOS.COMPANIA
    AND CASE WHEN PERSONAL.CARGO_ENCARGO IS NOT NULL
    AND ENCARGOS.FECHAINICIO <= PERIODOS.FECHAINICIO
    AND ENCARGOS.FECHAFINAL >= PERIODOS.FECHAFINAL
	     THEN  PERSONAL.CARGO_ENCARGO
         ELSE PERSONAL.ID_DE_CARGO
         END=CARGOS.ID_DE_CARGO
  LEFT JOIN ESCALAFON 
    ON CARGOS.COMPANIA=ESCALAFON.COMPANIA
    AND CARGOS.ESCALAFON=ESCALAFON.CODIGO
  LEFT JOIN DEPENDENCIA 
    ON PERSONAL.COMPANIA=DEPENDENCIA.COMPANIA
    AND PERSONAL.DEPENDENCIA=DEPENDENCIA.CODIGO
    
  WHERE  PERSONAL.COMPANIA= s$compania$s 
    AND PERSONAL.ESTADO_ACTUAL NOT IN (3)
    AND PERSONAL.ID_DE_EMPLEADO NOT IN (0)
    
  s$condicion$s