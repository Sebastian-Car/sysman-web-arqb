SELECT 
	ESTABLECIMIENTOS_DOCENTES.CODIGO ESTABLECIMIENTO, 
	ESTABLECIMIENTOS_DOCENTES.NOMBRE  NOMBRE_ESTABLECIMIENTO,
	CONCEPTOS.NOMBRE_CONCEPTO     NOMBRE_CONCEPTO,
	CASE WHEN ESTABLECIMIENTOS_DOCENTES.CODIGO IN('002') 
		   THEN CONCEPTOS.CTA_CONTRACRE_PPTAL 
		   ELSE CONCEPTOS.CTA_CRE_PPTAL 
	END CTA_CRE_PPTAL,
	SUM(HISTORICOS.VALOR) TOTAL
FROM HISTORICOS
	INNER JOIN PERSONAL
		ON HISTORICOS.COMPANIA      		  = PERSONAL.COMPANIA 
		AND HISTORICOS.ID_DE_EMPLEADO 		= PERSONAL.ID_DE_EMPLEADO
	INNER JOIN CONCEPTOS
		ON HISTORICOS.COMPANIA        		= CONCEPTOS.COMPANIA
		AND HISTORICOS.ID_DE_CONCEPTO 		= CONCEPTOS.ID_DE_CONCEPTO
	INNER JOIN ESTABLECIMIENTOS_DOCENTES 
		ON PERSONAL.COMPANIA 				        = ESTABLECIMIENTOS_DOCENTES.COMPANIA 
		AND PERSONAL.CODIGO_ESTABLECIMIENTO = ESTABLECIMIENTOS_DOCENTES.CODIGO
WHERE HISTORICOS.COMPANIA       = $P!{PR_COMPANIA}
  AND HISTORICOS.ID_DE_PROCESO  = $P!{PR_ID_DE_PROCESO}
  AND HISTORICOS.ANO            = $P!{PR_ANO}
  AND HISTORICOS.MES            = $P!{PR_MES}
  AND HISTORICOS.PERIODO        = $P!{PR_PERIODO}
  AND CONCEPTOS.CLASE           IN(3)
GROUP BY 
  ESTABLECIMIENTOS_DOCENTES.CODIGO,
  ESTABLECIMIENTOS_DOCENTES.NOMBRE,
  CONCEPTOS.NOMBRE_CONCEPTO,
  CASE WHEN ESTABLECIMIENTOS_DOCENTES.CODIGO IN('002') 
    THEN CONCEPTOS.CTA_CONTRACRE_PPTAL 
    ELSE CONCEPTOS.CTA_CRE_PPTAL 
  END,
  HISTORICOS.ID_DE_CONCEPTO
ORDER BY ESTABLECIMIENTOS_DOCENTES.NOMBRE,
  HISTORICOS.ID_DE_CONCEPTO
