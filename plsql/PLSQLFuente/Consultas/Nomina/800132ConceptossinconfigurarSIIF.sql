 SELECT 
        HISTORICOS.COMPANIA,
        CONCEPTOS.CLASE,
        SUM(HISTORICOS.VALOR) TOTAL,
        CONCEPTOS.ID_DE_CONCEPTO,
        CONCEPTOS.NITCONCEPTO,
        CONCEPTOS.NOMBRE_CONCEPTO || CASE WHEN V_FONDOS.COMPANIA IS NULL THEN ''     ELSE ' / ' || V_FONDOS.NOMBRE_FONDO END  NOMBRE_CONCEPTO,
        CASE WHEN V_FONDOS.COMPANIA IS NULL THEN CONCEPTOS.NITCONCEPTO     ELSE V_FONDOS.NIT          END NIT_SIIF,
        CASE WHEN V_FONDOS.COMPANIA IS NULL THEN TIPOS_DOCUMENTOS.DOC_SIIF ELSE TIPOFONDO.DOC_SIIF    END DOC_SIIF,
		CONCEPTOS.RUBROS_SIIF,
		CONCEPTOS.SIIF_ATRIBUTOEMPLEADO  CONCEPTOS_ATRIBUTOEMPLEADO,
		V_FONDOS.RUBRO_SIIF_EMPLEADO  V_FONDOS_ATRIBUTOEMPLEADO,
		CASE WHEN NVL(CONCEPTOS.SIIF_ATRIBUTOEMPLEADO, ' ') <> ' '
                         THEN CONCEPTOS.SIIF_ATRIBUTOEMPLEADO 
                         ELSE V_FONDOS.RUBRO_SIIF_EMPLEADO  END ENVIADO_ATRIBUTOEMPLEADO,						 
		CONCEPTOS.SIIF_ATRIBUTOPATRON  CONCEPTOS_ATRIBUTOPATRON,
		V_FONDOS.RUBRO_SIIF_PATRONO  V_FONDOS_ATRIBUTOPATRON,
		CASE WHEN NVL(CONCEPTOS.SIIF_ATRIBUTOPATRON, ' ') <> ' '
                              THEN CONCEPTOS.SIIF_ATRIBUTOPATRON 
                              ELSE V_FONDOS.RUBRO_SIIF_PATRONO  END ENVIADO_ATRIBUTOPATRON,	
        CASE WHEN V_FONDOS.COMPANIA IS NULL  OR CONCEPTOS.ID_DE_CONCEPTO IN(120,132,127) THEN 'NO' ELSE CASE WHEN V_FONDOS.PUBLICO IN(0) THEN 'PRIVADO' ELSE 'PÚBLICO' END END PORFONDO,        
        CASE WHEN NVL(CONCEPTOS.RUBROS_SIIF,' ')=' ' THEN 'SI' ELSE 'NO' END SIN_RUBRO,
        CASE WHEN (CONCEPTOS.CLASE = 8 AND NVL(CONCEPTOS.SIIF_ATRIBUTOPATRON  , ' ') = ' ' AND NVL(V_FONDOS.RUBRO_SIIF_PATRONO , ' ') = ' ')
               OR (CONCEPTOS.CLASE = 5 AND NVL(CONCEPTOS.SIIF_ATRIBUTOEMPLEADO, ' ') = ' ' AND NVL(V_FONDOS.RUBRO_SIIF_EMPLEADO, ' ') = ' ')
        THEN 'SI' ELSE CASE WHEN CONCEPTOS.CLASE = 3 THEN 'NO_APLICA' ELSE 'NO' END END SIN_ATRIBUTO
    FROM PERSONAL INNER JOIN HISTORICOS
       ON HISTORICOS.COMPANIA       = PERSONAL.COMPANIA
      AND HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
    INNER JOIN CONCEPTOS
       ON HISTORICOS.COMPANIA       = CONCEPTOS.COMPANIA
      AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
    INNER JOIN PERIODOS
       ON HISTORICOS.COMPANIA      = PERIODOS.COMPANIA
      AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO
      AND HISTORICOS.ANO           = PERIODOS.ANO
      AND HISTORICOS.MES           = PERIODOS.MES
      AND HISTORICOS.PERIODO       = PERIODOS.PERIODO
    LEFT JOIN TIPOS_DOCUMENTOS
      ON  CONCEPTOS.COMPANIA            =   TIPOS_DOCUMENTOS.COMPANIA
      AND CONCEPTOS.DOC_SIIF_CONCEPTO   =   TIPOS_DOCUMENTOS.DCTO_IDENTIDAD                
    LEFT JOIN  V_FONDOS
        ON PERSONAL.COMPANIA    = V_FONDOS.COMPANIA
       AND CASE WHEN CONCEPTOS.ID_DE_CONCEPTO IN(113, 130, 123)                                
                THEN PERSONAL.FONDO_SALUD 
                ELSE CASE WHEN CONCEPTOS.ID_DE_CONCEPTO IN(120,131,132)                       
                          THEN PERSONAL.ID_DEL_FONDO 
                          ELSE CASE WHEN CONCEPTOS.ID_DE_CONCEPTO IN(124)                       
                                    THEN PERSONAL.FONDO_PENSION_VOL 
                                    ELSE CASE WHEN CONCEPTOS.ID_DE_CONCEPTO IN(127)            
                                              THEN PERSONAL.FONDO_AFC 
                                              ELSE CASE WHEN CONCEPTOS.ID_DE_CONCEPTO IN(100, 200, 205)    
                                                        THEN PERSONAL.MEDICINA_PREPAGADA 
                                                        ELSE ' '
                                                   END 
                                         END
                               END         
                      END
            END = V_FONDOS.COD_FONDO
    LEFT JOIN TIPOS_DOCUMENTOS TIPOFONDO
       ON V_FONDOS.COMPANIA   = TIPOFONDO.COMPANIA
      AND V_FONDOS.DOC_SIIF   = TIPOFONDO.DCTO_IDENTIDAD
    WHERE PERSONAL.COMPANIA         = s$compania$s
      AND HISTORICOS.ANO            = s$anio$s 
      AND PERIODOS.ACUMULADO NOT IN(0)      
	  AND ((CONCEPTOS.CLASE IN(8)
	  AND CONCEPTOS.ID_DE_CONCEPTO < 490 
	  AND CONCEPTOS.ID_DE_CONCEPTO NOT IN(119, 483)
	  ) OR (CONCEPTOS.CLASE IN(3, 5))
        OR (CONCEPTOS.CLASE IN(3) AND (CONCEPTOS.ID_DE_CONCEPTO BETWEEN 600 AND 698        
                    OR CONCEPTOS.ID_DE_CONCEPTO BETWEEN 700 AND 798         
                    OR CONCEPTOS.ID_DE_CONCEPTO IN(113,130,123,            
                                                 120,131,132,            
                                                 125,126,225,            
                                                 124,                    
                                                 127,                   
                                                 100, 200, 205
                                                ))))
    GROUP BY HISTORICOS.COMPANIA,
             CONCEPTOS.CLASE,   
             CONCEPTOS.ID_DE_CONCEPTO,
             CONCEPTOS.NITCONCEPTO,
             CONCEPTOS.NOMBRE_CONCEPTO || CASE WHEN V_FONDOS.COMPANIA IS NULL THEN ''     ELSE ' / ' || V_FONDOS.NOMBRE_FONDO END,
             CASE WHEN V_FONDOS.COMPANIA IS NULL THEN CONCEPTOS.NITCONCEPTO     ELSE V_FONDOS.NIT          END,
             CASE WHEN V_FONDOS.COMPANIA IS NULL THEN TIPOS_DOCUMENTOS.DOC_SIIF ELSE TIPOFONDO.DOC_SIIF    END,
            CONCEPTOS.RUBROS_SIIF,
		CONCEPTOS.SIIF_ATRIBUTOEMPLEADO  ,
		V_FONDOS.RUBRO_SIIF_EMPLEADO   ,
		CASE WHEN NVL(CONCEPTOS.SIIF_ATRIBUTOEMPLEADO, ' ') <> ' '
                         THEN CONCEPTOS.SIIF_ATRIBUTOEMPLEADO 
                         ELSE V_FONDOS.RUBRO_SIIF_EMPLEADO  END ,						 
		CONCEPTOS.SIIF_ATRIBUTOPATRON  ,
		V_FONDOS.RUBRO_SIIF_PATRONO  ,
		CASE WHEN NVL(CONCEPTOS.SIIF_ATRIBUTOPATRON, ' ') <> ' '
                              THEN CONCEPTOS.SIIF_ATRIBUTOPATRON 
                              ELSE V_FONDOS.RUBRO_SIIF_PATRONO  END ,	
        CASE WHEN V_FONDOS.COMPANIA IS NULL  OR CONCEPTOS.ID_DE_CONCEPTO IN(120,132,127) THEN 'NO' ELSE CASE WHEN V_FONDOS.PUBLICO IN(0) THEN 'PRIVADO' ELSE 'PÚBLICO' END END  ,
        CASE WHEN NVL(CONCEPTOS.RUBROS_SIIF,' ')=' ' THEN 'SI' ELSE 'NO' END,
        CASE WHEN (CONCEPTOS.CLASE = 8 AND NVL(CONCEPTOS.SIIF_ATRIBUTOPATRON  , ' ') = ' ' AND NVL(V_FONDOS.RUBRO_SIIF_PATRONO , ' ') = ' ')
               OR (CONCEPTOS.CLASE = 5 AND NVL(CONCEPTOS.SIIF_ATRIBUTOEMPLEADO, ' ') = ' ' AND NVL(V_FONDOS.RUBRO_SIIF_EMPLEADO, ' ') = ' ')
        THEN 'SI' ELSE CASE WHEN CONCEPTOS.CLASE = 3 THEN 'NO_APLICA' ELSE 'NO' END END 
    ORDER BY CONCEPTOS.CLASE, CONCEPTOS.ID_DE_CONCEPTO
