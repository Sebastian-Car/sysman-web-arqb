SELECT DISTINCT
PERSONAL.CAJA_COMPENSACION,
PERSONAL.NOMBRECOMPLETO,
PERSONAL.NUMERO_DCTO, 
V_CAJA_COMPENSACION.NIT,
V_CAJA_COMPENSACION.NOMBRE_CAJA, 
ESTABLECIMIENTOS_DOCENTES.CODIGO AS ID_FUENTE,
ESTABLECIMIENTOS_DOCENTES.NOMBRE AS FUENTE,
HISTORICOS.COMPANIA, 
HISTORICOS.ID_DE_PROCESO, 
HISTORICOS.ANO, 
HISTORICOS.MES, 
HISTORICOS.PERIODO,
PCK_SYSMAN_UTL.FC_NOMBRE_MES(Historicos.MES)  NOMBRE_MES,
PERIODOS.NOM_PERIODO NOMBRE_PERIODO,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '001' THEN HISTORICOS.VALOR ELSE 0 END) AS SALARIO_BASE,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '101' THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '102' THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE_ICBF,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '103' THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE_SENA,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '104' THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE_ESAP,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '105' THEN HISTORICOS.VALOR ELSE 0 END) AS APORTE_INSTITUTOS_TECNICOS, 
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '108' THEN HISTORICOS.VALOR ELSE 0 END) AS BASE_PARAFISCAL,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('080','979','525','533') THEN HISTORICOS.VALOR ELSE 0 END) AS TRANSPORTE,
ROUND(SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '108' THEN HISTORICOS.VALOR ELSE 0 END)*9/100,0) AS TOTAL,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('101','102','103','104','105') THEN HISTORICOS.VALOR ELSE 0 END) AS SUMA,
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '108' THEN HISTORICOS.VALOR ELSE 0 END) -
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO IN ('080','979','525','533') THEN HISTORICOS.VALOR ELSE 0 END) BASE_PARAFISCAL2,
PERSONAL.ID_DE_EMPLEADO, 
HISTORICOS.PERIODO
FROM 
HISTORICOS 
LEFT JOIN PERSONAL 
ON HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
AND HISTORICOS.COMPANIA = PERSONAL.COMPANIA
LEFT JOIN V_CAJA_COMPENSACION
ON PERSONAL.CAJA_COMPENSACION = V_CAJA_COMPENSACION.CAJA_COMPENSACION
AND PERSONAL.COMPANIA = V_CAJA_COMPENSACION.COMPANIA 
LEFT JOIN ESTABLECIMIENTOS_DOCENTES 
ON PERSONAL.COMPANIA = ESTABLECIMIENTOS_DOCENTES.COMPANIA 
AND PERSONAL.CODIGO_ESTABLECIMIENTO = ESTABLECIMIENTOS_DOCENTES.CODIGO

LEFT JOIN PERIODOS 
ON HISTORICOS.COMPANIA = PERIODOS.COMPANIA
AND HISTORICOS.ID_DE_PROCESO = PERIODOS.ID_DE_PROCESO 
AND HISTORICOS.ANO = PERIODOS.ANO 
AND HISTORICOS.MES = PERIODOS.MES 
AND HISTORICOS.PERIODO = PERIODOS.PERIODO

WHERE
HISTORICOS.ID_DE_CONCEPTO IN ('001', '101', '102', '103', '108', '104', '105', '107', '080', '979', '525', '533')
AND HISTORICOS.COMPANIA = s$compania$s
AND HISTORICOS.ID_DE_PROCESO = s$proceso$s
AND HISTORICOS.ANO = s$ano1$s
AND HISTORICOS.MES = s$mes1$s
AND PERIODOS.ACUMULADO NOT IN (0)
GROUP BY
PERSONAL.CAJA_COMPENSACION, 
PERSONAL.NOMBRECOMPLETO,
PERSONAL.NUMERO_DCTO, 
V_CAJA_COMPENSACION.NIT, 
V_CAJA_COMPENSACION.NOMBRE_CAJA,
ESTABLECIMIENTOS_DOCENTES.CODIGO, 
ESTABLECIMIENTOS_DOCENTES.NOMBRE, 
HISTORICOS.COMPANIA, 
HISTORICOS.ID_DE_PROCESO, 
HISTORICOS.ANO, 
HISTORICOS.MES, 
HISTORICOS.PERIODO, 
PERSONAL.ID_DE_EMPLEADO, 
PERSONAL.ID_CENTRO_DE_COSTO, 
HISTORICOS.PERIODO,
PERIODOS.NOM_PERIODO
HAVING
SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '108' THEN HISTORICOS.VALOR ELSE 0 END)<> 0