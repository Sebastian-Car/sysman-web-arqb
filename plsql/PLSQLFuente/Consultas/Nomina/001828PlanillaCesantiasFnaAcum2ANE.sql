SELECT 
  PERSONAL.NUMERO_DCTO   AS CEDULA,  
  PERSONAL.APELLIDO1 || ' ' || PERSONAL.APELLIDO2 || ' ' || PERSONAL.NOMBRES AS NOMBRES,
  PERSONAL.FECHA_DE_INGRESO,
  PERSONAL.FECHA_DE_RETIRO,
  PERSONAL.FECHAFONDOCESANTIA,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '910' THEN HISTORICOS.VALOR ELSE 0 END) AS C910,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '912' THEN HISTORICOS.VALOR ELSE 0 END) AS C912,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '900' THEN HISTORICOS.VALOR ELSE 0 END) AS C900,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '903' THEN HISTORICOS.VALOR ELSE 0 END) AS C903,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '904' THEN HISTORICOS.VALOR ELSE 0 END) AS C904,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '914' THEN HISTORICOS.VALOR ELSE 0 END) AS C914,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '901' THEN HISTORICOS.VALOR ELSE 0 END) AS C901,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '906' THEN HISTORICOS.VALOR ELSE 0 END) AS C906,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '907' THEN HISTORICOS.VALOR ELSE 0 END) AS C907,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '905' THEN HISTORICOS.VALOR ELSE 0 END) AS C905,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '902' THEN HISTORICOS.VALOR ELSE 0 END) AS C902,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '913' THEN HISTORICOS.VALOR ELSE 0 END) AS C913,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '277' THEN HISTORICOS.VALOR ELSE 0 END) +
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '483' THEN HISTORICOS.VALOR ELSE 0 END) +
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '915' THEN HISTORICOS.VALOR ELSE 0 END) AS CESANTIAS_ACUM,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '915' THEN HISTORICOS.VALOR ELSE 0 END) AS C915,
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '277' THEN HISTORICOS.VALOR ELSE 0 END) +
  SUM(CASE WHEN HISTORICOS.ID_DE_CONCEPTO = '483' THEN HISTORICOS.VALOR ELSE 0 END) AS C483  
FROM HISTORICOS
 INNER JOIN PERSONAL_HISTORICO PERSONAL
    ON      HISTORICOS.COMPANIA           = PERSONAL.COMPANIA
   AND      HISTORICOS.ID_DE_PROCESO      = PERSONAL.ID_DE_PROCESO
   AND      HISTORICOS.ANO                = PERSONAL.ANO
   AND      HISTORICOS.MES                = PERSONAL.MES
   AND      HISTORICOS.PERIODO            = PERSONAL.PERIODO
   AND      HISTORICOS.ID_DE_EMPLEADO     = PERSONAL.ID_DE_EMPLEADO
WHERE HISTORICOS.COMPANIA         = s$compania$s
  AND HISTORICOS.ID_DE_PROCESO    = s$proceso$s 
  AND HISTORICOS.ANO              = s$anio$s 
  AND HISTORICOS.MES              = s$mes$s 
  AND HISTORICOS.PERIODO          = s$periodo$s 
  AND HISTORICOS.ID_DE_CONCEPTO  IN ('001','993','994','911','009','277','483','900','901','902','903','904','905','906','907','908','909','910','911','912','913','914','915')
  AND PERSONAL.FONDO_CESANTIAS  = PCK_SYSMAN_UTL.FC_PAR( UN_COMPANIA  => s$compania$s 
                                                        ,UN_NOMBRE    => 'FONDO NACIONAL AHORRO'
                                                        ,UN_MODULO    => 6
                                                        ,UN_FECHA_PAR => SYSDATE
                                                        ,UN_IND_MAYUS => 0) 
GROUP BY 
  PERSONAL.NUMERO_DCTO, 
  PERSONAL.APELLIDO1 || ' ' || PERSONAL.APELLIDO2 || ' ' || PERSONAL.NOMBRES ,  
  PERSONAL.FECHA_DE_INGRESO,
  PERSONAL.FECHA_DE_RETIRO,
  PERSONAL.FECHAFONDOCESANTIA
