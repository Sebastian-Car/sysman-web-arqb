SELECT 
   PERSONAL.NOMBRECOMPLETO NOMCOMPLETO
  ,CAMBIOSDEFONDO.TIPOFONDO
  ,CLASEDEFONDO.NOMBRE     NOMBRETIPOFONDO
  ,CAMBIOSDEFONDO.FONDOACTUAL
  ,CAMBIOSDEFONDO.FONDONUEVO
  ,FONDO.NOMBRE_FONDO      NOMBREFONDO
  ,PERSONAL.NUMERO_DCTO
  ,TO_CHAR(CAMBIOSDEFONDO.FECHAINICIAL  ,'DD/MM/YYYY') FECHAINICIAL
  ,TO_CHAR(CAMBIOSDEFONDO.FECHA_TRASLADO,'DD/MM/YYYY') FECHA_TRASLADO
FROM PERSONAL 
  INNER JOIN CAMBIOSDEFONDO 
     ON PERSONAL.COMPANIA       = CAMBIOSDEFONDO.COMPANIA
    AND PERSONAL.ID_DE_EMPLEADO = CAMBIOSDEFONDO.ID_DE_EMPLEADO
   LEFT JOIN CLASEFONDO 
     ON CAMBIOSDEFONDO.COMPANIA    = CLASEFONDO.COMPANIA
    AND CAMBIOSDEFONDO.FONDOACTUAL = CLASEFONDO.ID
   LEFT JOIN FONDO
     ON CLASEFONDO.COMPANIA    = FONDO.COMPANIA
    AND CLASEFONDO.ID_DE_FONDO = FONDO.ID_DE_FONDO
   LEFT JOIN CLASEDEFONDO
     ON CLASEFONDO.CLASE = CLASEDEFONDO.CODIGO 
WHERE CAMBIOSDEFONDO.COMPANIA       = s$compania$s 
  AND CAMBIOSDEFONDO.ID_DE_EMPLEADO = NVL(s$idEmpleado$s,CAMBIOSDEFONDO.ID_DE_EMPLEADO)
  AND (   CAMBIOSDEFONDO.FECHAINICIAL   BETWEEN s$fechaInicialAux$s AND s$fechaFinalAux$s 
       OR CAMBIOSDEFONDO.FECHA_TRASLADO BETWEEN s$fechaInicialAux$s And s$fechaFinalAux$s)
ORDER BY
   PERSONAL.NOMBRECOMPLETO
  ,CAMBIOSDEFONDO.FECHAINICIAL
