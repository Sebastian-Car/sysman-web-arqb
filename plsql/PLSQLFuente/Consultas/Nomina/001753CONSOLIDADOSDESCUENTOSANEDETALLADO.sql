  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  5 ORDEN,
  'LIBRANZAS' NOMBRE_CONCEPTO,
CASE WHEN NVL(CONCEPTOS.SIIF_ATRIBUTOEMPLEADO, ' ') <> ' '
       THEN CONCEPTOS.SIIF_ATRIBUTOEMPLEADO 
       ELSE V_FONDOS.RUBRO_SIIF_EMPLEADO  END CODIGO_DESCUENTO,
  SUM (HISTORICOS.VALOR) VALOR_TOTAL ,
  0 VALOR_MES_SIGUIENTE,
  0 VALOR_2_MESES,
  SUM (HISTORICOS.VALOR) TOTALVALOR,
  PERSONAL.NUMERO_DCTO CEDULA,
  PERSONAL.ID_DE_EMPLEADO,
  PERSONAL.NOMBRECOMPLETO
FROM HISTORICOS
INNER JOIN CONCEPTOS
ON HISTORICOS.COMPANIA        = CONCEPTOS.COMPANIA
AND HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
INNER JOIN PERSONAL
ON HISTORICOS.COMPANIA             = PERSONAL.COMPANIA
AND HISTORICOS.ID_DE_EMPLEADO      = PERSONAL.ID_DE_EMPLEADO
INNER JOIN V_FONDOS
ON PERSONAL.COMPANIA                  = V_FONDOS.COMPANIA
AND PERSONAL.ID_DEL_FONDO             = V_FONDOS.COD_FONDO
WHERE HISTORICOS.COMPANIA          = s$compania$s
AND HISTORICOS.ID_DE_PROCESO       = s$proceso$s
AND HISTORICOS.ANO                 = s$anio$s
AND HISTORICOS.MES                 = s$mes$s
AND HISTORICOS.PERIODO             = s$periodo$s
AND CONCEPTOS.TIPOCONSOLIDADO     IN ('LIB')
AND CONCEPTOS.TIPOCONSOLIDADO NOT IN ('PER' ,'PEC','PVI')
GROUP BY HISTORICOS.COMPANIA,
  HISTORICOS.ID_DE_PROCESO,
  HISTORICOS.ANO,
  HISTORICOS.MES,
  HISTORICOS.PERIODO,
  5,
CASE WHEN NVL(CONCEPTOS.SIIF_ATRIBUTOEMPLEADO, ' ') <> ' '
       THEN CONCEPTOS.SIIF_ATRIBUTOEMPLEADO 
       ELSE V_FONDOS.RUBRO_SIIF_EMPLEADO  END,
  PERSONAL.NUMERO_DCTO ,
  PERSONAL.ID_DE_EMPLEADO,
  PERSONAL.NOMBRECOMPLETO
