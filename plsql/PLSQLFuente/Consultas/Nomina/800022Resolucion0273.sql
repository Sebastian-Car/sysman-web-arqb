SELECT *
FROM (
SELECT  PERSONAL_HISTORICO.NOMBRECOMPLETO, 
        PERSONAL_HISTORICO.NUMERO_DCTO,
        'NO' PEXONERADO, 
        PERSONAL_HISTORICO.DIRECCION,
        PERSONAL_HISTORICO.TELEFONOS,
        PERSONAL_HISTORICO.NOMBRE_CIUDAD_HAB,
        V_ACUMULADOS.VALOR,
        CONCEPTOS.RES273_2013,
        SUM(CASE WHEN V_ACUMULADOS.CLASE = 3 THEN V_ACUMULADOS.VALOR ELSE 0 END)  OVER (Partition BY V_ACUMULADOS.ID_DE_EMPLEADO) TOTAL
FROM V_ACUMULADOS 
INNER JOIN PERSONAL_HISTORICO
  ON (V_ACUMULADOS.COMPANIA = PERSONAL_HISTORICO.COMPANIA) 
 AND (V_ACUMULADOS.ID_DE_PROCESO = PERSONAL_HISTORICO.ID_DE_PROCESO)  
 AND (V_ACUMULADOS.ANO = PERSONAL_HISTORICO.ANO)  
 AND (V_ACUMULADOS.MES = PERSONAL_HISTORICO.MES)
 AND (V_ACUMULADOS.PERIODO = PERSONAL_HISTORICO.PERIODO) 
 AND (V_ACUMULADOS.ID_DE_EMPLEADO = PERSONAL_HISTORICO.ID_DE_EMPLEADO)
INNER JOIN CONCEPTOS ON (V_ACUMULADOS.COMPANIA = CONCEPTOS.COMPANIA) AND (V_ACUMULADOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO)
WHERE V_ACUMULADOS.COMPANIA = s$compania$s
  AND V_ACUMULADOS.ID_DE_PROCESO = s$idProceso$s
  AND TRIM(TO_CHAR(V_ACUMULADOS.ANO, '0000')) || TRIM(TO_CHAR(V_ACUMULADOS.MES, '00')) || TRIM(TO_CHAR(V_ACUMULADOS.PERIODO, '00')) BETWEEN s$rangoInicial$s  AND s$rangoFinal$s  
  AND V_ACUMULADOS.CLASE IN (3,5)
  ) PSUB
  PIVOT ( SUM(VALOR) FOR RES273_2013 IN ('10_SUEL','11_TRAN','12_CESA','13_CESP','14_INTC','15_PSER','16_PRPA','17_VACA','18_VACP','19_BONO','20_VIAT','22_RETE') )
  ORDER  BY 1
