SELECT 
    PERSONAL_HISTORICO.COMPANIA, 
    PERSONAL_HISTORICO.ID_DE_PROCESO, 
    PERSONAL_HISTORICO.ANO, 
    UPPER(PCK_SYSMAN_UTL.FC_NOMBRE_MES(PERSONAL_HISTORICO.MES))  NOMBREMES, 
    PERSONAL_HISTORICO.PERIODO, 
    PERSONAL_HISTORICO.NOMBRECOMPLETO, 
    PERSONAL_HISTORICO.NUMERO_DCTO, 
    PERSONAL_HISTORICO.ID_DE_EMPLEADO, 
    PERSONAL_HISTORICO.FECHA_DE_INGRESO, 
    PERSONAL_HISTORICO.ID_CENTRO_DE_COSTO, 
    PERSONAL_HISTORICO.NOMBRE_CENTRO_DE_COSTO, 
    SUM(CASE WHEN 
            HISTORICOS.ID_DE_CONCEPTO = 945
        THEN 
            VALOR
        ELSE
            0
        END)  SUELDOJUN, 
    SUM(CASE WHEN 
            HISTORICOS.ID_DE_CONCEPTO = 950
        THEN 
            VALOR
        ELSE
            0
        END)  GASTOSREP, 
    SUM(CASE WHEN 
            HISTORICOS.ID_DE_CONCEPTO = 948
        THEN 
            VALOR
        ELSE
            0
        END)  TRANSPJUN, 
    SUM(CASE WHEN 
            HISTORICOS.ID_DE_CONCEPTO = 949
        THEN 
            VALOR
        ELSE
            0
        END)  ALIMENJUN, 
    SUM(CASE WHEN 
            HISTORICOS.ID_DE_CONCEPTO = 947
        THEN 
            VALOR
        ELSE
            0
        END)  EXTRASJUN, 
    SUM(CASE WHEN 
            HISTORICOS.ID_DE_CONCEPTO = 951
        THEN 
            VALOR
        ELSE
            0
        END)  DIASPJUN, 
    SUM(CASE WHEN 
            HISTORICOS.ID_DE_CONCEPTO = 952
        THEN 
            VALOR
        ELSE
            0
        END)  DIASCOMJUN, 
    SUM(CASE WHEN 
            HISTORICOS.ID_DE_CONCEPTO = 160
        THEN 
            VALOR
        ELSE
            0
        END)  PRIMAJUN, 
    SUM(CASE WHEN 
            HISTORICOS.ID_DE_CONCEPTO = 140
        THEN 
            VALOR
        ELSE
            0
        END)  DESCUENTOS, 
    SUM(CASE WHEN 
            HISTORICOS.ID_DE_CONCEPTO = 144
        THEN 
            VALOR
        ELSE
            0
        END)  NETO 
FROM 
    PERSONAL_HISTORICO 
        INNER JOIN HISTORICOS 
        ON 
            (PERSONAL_HISTORICO.COMPANIA = HISTORICOS.COMPANIA) 
                AND 
            (PERSONAL_HISTORICO.ID_DE_PROCESO = HISTORICOS.ID_DE_PROCESO) 
                AND 
            (PERSONAL_HISTORICO.ANO = HISTORICOS.ANO) 
                AND 
            (PERSONAL_HISTORICO.MES = HISTORICOS.MES) 
                AND 
            (PERSONAL_HISTORICO.PERIODO = HISTORICOS.PERIODO) 
                AND 
            (PERSONAL_HISTORICO.ID_DE_EMPLEADO = HISTORICOS.ID_DE_EMPLEADO) 
WHERE 
     PERSONAL_HISTORICO.COMPANIA = s$compania$s 
 AND PERSONAL_HISTORICO.ID_DE_PROCESO = s$idProceso$s
 AND PERSONAL_HISTORICO.ANO = s$ano$s 
 AND PERSONAL_HISTORICO.MES = s$mes$s
 AND PERSONAL_HISTORICO.PERIODO = s$periodo$s
 AND PERSONAL_HISTORICO.ID_DE_TIPO NOT IN (2)
GROUP BY 
    PERSONAL_HISTORICO.COMPANIA, 
    PERSONAL_HISTORICO.ID_DE_PROCESO, 
    PERSONAL_HISTORICO.ANO, 
    UPPER(PCK_SYSMAN_UTL.FC_NOMBRE_MES(PERSONAL_HISTORICO.MES)), 
    PERSONAL_HISTORICO.PERIODO, 
    PERSONAL_HISTORICO.NOMBRECOMPLETO, 
    PERSONAL_HISTORICO.NUMERO_DCTO, 
    PERSONAL_HISTORICO.ID_DE_EMPLEADO, 
    PERSONAL_HISTORICO.FECHA_DE_INGRESO, 
    PERSONAL_HISTORICO.ID_CENTRO_DE_COSTO, 
    PERSONAL_HISTORICO.NOMBRE_CENTRO_DE_COSTO, 
    PERSONAL_HISTORICO.NOMBRE_DE_CARGO, 
    PERSONAL_HISTORICO.NOMBRE_FORMANOMBRAMIENTO, 
    PERSONAL_HISTORICO.CAUSA_RETIRO, 
    PERSONAL_HISTORICO.FECHATERCONTRATO, 
    PERSONAL_HISTORICO.NOMBRE_CENTRO_DE_COSTO 
HAVING  SUM(CASE WHEN 
            HISTORICOS.ID_DE_CONCEPTO = 160
        THEN 
            VALOR
        ELSE
            0
        END) >0
ORDER BY 
    PERSONAL_HISTORICO.NOMBRECOMPLETO
