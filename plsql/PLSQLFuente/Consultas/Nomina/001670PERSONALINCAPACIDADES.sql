SELECT DISTINCT V_PERSONAL.NUMERO_DCTO,
  V_PERSONAL.APELLIDO1
  ||' '
  ||V_PERSONAL.APELLIDO2
  ||' '
  ||V_PERSONAL.NOMBRES NOMCOMPLETO,
  's$nombreEstado$s' ESTADO_ACTUAL,
  V_PERSONAL.NOMBRE_FONDOSALUD NOMBRE_FONDO_SALUD,
  INCAPACIDADES.FECHA_INICIO,
  INCAPACIDADES.FECHA_FIN,
  INCAPACIDADES.FECHAINIREAL,
  INCAPACIDADES.FECHAFINREAL,
  INCAPACIDADES.TIPOENFERMEDAD,
  TIPO_ENFERMEDADES.NOMBRE_ENFERMEDAD,
  INCAPACIDADES.DIAS,
  V_PERSONAL.SEXO,
  V_PERSONAL.NOMBRE_DEPENDENCIA
,
  TIPO_INCAPACIDAD.DESCRIPCION
FROM V_PERSONAL
INNER JOIN INCAPACIDADES
ON V_PERSONAL.COMPANIA        = INCAPACIDADES.COMPANIA
AND V_PERSONAL.ID_DE_EMPLEADO = INCAPACIDADES.ID_DE_EMPLEADO
LEFT JOIN TIPO_INCAPACIDAD
ON INCAPACIDADES.COMPANIA   = TIPO_INCAPACIDAD.COMPANIA
AND  INCAPACIDADES.INCAPACIDAD = TIPO_INCAPACIDAD.INCAPACIDAD
LEFT JOIN TIPO_ENFERMEDADES
ON INCAPACIDADES.TIPOENFERMEDAD    = TIPO_ENFERMEDADES.CODIGOEN
WHERE V_PERSONAL.COMPANIA          = s$compania$s
AND V_PERSONAL.ID_DE_PROCESO       = s$procesoNomina$s
AND V_PERSONAL.ANO                 = s$anoNomina$s
AND V_PERSONAL.MES                 = s$mesNomina$s
AND V_PERSONAL.PERIODO             = s$periodoNomina$s
AND V_PERSONAL.ESTADO_ACTUAL       = s$estadoActual$s
AND V_PERSONAL.ID_DE_EMPLEADO NOT IN (0)
AND V_PERSONAL.SEXO                =
  CASE
    WHEN 'T' = 'T'
    THEN V_PERSONAL.SEXO
    ELSE 'T'
  END
AND V_PERSONAL.ID_DE_CARGO BETWEEN 's$cargoInicial$s' AND 's$cargoFinal$s'
AND V_PERSONAL.DE_CARRERA BETWEEN 's$tipoContratoInicial$s' AND 's$tipoContratoFinal$s'
AND V_PERSONAL.FECHANCTO BETWEEN TO_DATE('s$fechaInicialNacimiento$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinalNacimiento$s','DD/MM/YYYY')
AND INCAPACIDADES.FECHA_INICIO BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
AND INCAPACIDADES.INCAPACIDAD BETWEEN 's$incapacidadInicial$s' AND 's$incapacidadFinal$s'
AND INCAPACIDADES.TIPOENFERMEDAD BETWEEN 's$enfermedadInicial$s' AND 's$enfermedadFinal$s'
