WITH CRA688_MESANT AS
  (SELECT HISTORICOS.COMPANIA,
    PERSONAL.DEPENDENCIA,
    DEPENDENCIA.NOMBRE AS NOMBREDEPENDENCIA,
    CARGOS.NOMBRE_DEL_CARGO,
    PERSONAL.ID_CENTRO_DE_COSTO,
    CENTRO_COSTO.NOMBRE,
    PERSONAL.APELLIDO1
    || ' '
    || PERSONAL.APELLIDO2
    || ' '
    || PERSONAL.NOMBRES   AS NOMBRECOMPLETO,
    SUM(HISTORICOS.VALOR) AS TOTALMESANT,
    PERSONAL.ID_DE_EMPLEADO,
    PERSONAL.CMACMO
  FROM HISTORICOS
  INNER JOIN PERSONAL
  ON HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
  AND HISTORICOS.COMPANIA      = PERSONAL.COMPANIA
  INNER JOIN CONCEPTOS
  ON HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
  AND HISTORICOS.COMPANIA      = CONCEPTOS.COMPANIA
  INNER JOIN DEPENDENCIA
  ON PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
  AND PERSONAL.COMPANIA   = DEPENDENCIA.COMPANIA
  INNER JOIN CARGOS
  ON PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO
  AND PERSONAL.COMPANIA   = CARGOS.COMPANIA
  INNER JOIN CENTRO_COSTO
  ON PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
  AND PERSONAL.COMPANIA          = CENTRO_COSTO.COMPANIA
  WHERE ( PERSONAL.COMPANIA      = s$compania$s
  AND HISTORICOS.ANO             =s$ano2$s
  AND HISTORICOS.MES             < LPAD(s$mes2$s, 2)
  AND CONCEPTOS.CLASE           IN (3))
  OR ( PERSONAL.COMPANIA         = s$compania$s
  AND HISTORICOS.ANO             =s$ano2$s
  AND HISTORICOS.MES             <LPAD(s$mes2$s, 2)
  AND CONCEPTOS.CLASE           IN (8)
  AND HISTORICOS.ID_DE_CONCEPTO BETWEEN '490' AND '499' )
  GROUP BY HISTORICOS.COMPANIA,
    PERSONAL.DEPENDENCIA,
    DEPENDENCIA.NOMBRE,
    CARGOS.NOMBRE_DEL_CARGO,
    PERSONAL.ID_CENTRO_DE_COSTO,
    CENTRO_COSTO.NOMBRE,
    PERSONAL.APELLIDO1
    || ' '
    || PERSONAL.APELLIDO2
    || ' '
    || PERSONAL.NOMBRES,
    PERSONAL.ID_DE_EMPLEADO,
    PERSONAL.CMACMO
  ORDER BY PERSONAL.DEPENDENCIA,
    PERSONAL.ID_CENTRO_DE_COSTO,
    PERSONAL.APELLIDO1
    || ' '
    || PERSONAL.APELLIDO2
    || ' '
    || PERSONAL.NOMBRES
  ),
  CRA688_MESACTUAL AS
  (SELECT HISTORICOS.COMPANIA,
    PERSONAL.DEPENDENCIA,
    DEPENDENCIA.NOMBRE AS NOMBREDEPENDENCIA,
    CARGOS.NOMBRE_DEL_CARGO,
    PERSONAL.ID_CENTRO_DE_COSTO,
    CENTRO_COSTO.NOMBRE,
    PERSONAL.APELLIDO1
    || ' '
    || PERSONAL.APELLIDO2
    || ' '
    || PERSONAL.NOMBRES   AS NOMBRECOMPLETO,
    SUM(HISTORICOS.VALOR) AS TOTALMESACTUAL,
    PERSONAL.ID_DE_EMPLEADO,
    PERSONAL.CMACMO
  FROM HISTORICOS
  INNER JOIN PERSONAL
  ON HISTORICOS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
  AND HISTORICOS.COMPANIA      = PERSONAL.COMPANIA
  INNER JOIN CONCEPTOS
  ON HISTORICOS.ID_DE_CONCEPTO = CONCEPTOS.ID_DE_CONCEPTO
  AND HISTORICOS.COMPANIA      = CONCEPTOS.COMPANIA
  INNER JOIN DEPENDENCIA
  ON PERSONAL.DEPENDENCIA = DEPENDENCIA.CODIGO
  AND PERSONAL.COMPANIA   = DEPENDENCIA.COMPANIA
  INNER JOIN CARGOS
  ON PERSONAL.ID_DE_CARGO = CARGOS.ID_DE_CARGO
  AND PERSONAL.COMPANIA   = CARGOS.COMPANIA
  INNER JOIN CENTRO_COSTO
  ON PERSONAL.ID_CENTRO_DE_COSTO = CENTRO_COSTO.CODIGO
  AND PERSONAL.COMPANIA          = CENTRO_COSTO.COMPANIA
  WHERE ( PERSONAL.COMPANIA      = s$compania$s
  AND HISTORICOS.ANO             =s$ano2$s
  AND HISTORICOS.MES             =LPAD(s$mes2$s, 2)
  AND CONCEPTOS.CLASE           IN (3))
  OR ( PERSONAL.COMPANIA         = s$compania$s
  AND HISTORICOS.ANO             =s$ano2$s
  AND HISTORICOS.MES             =LPAD(s$mes2$s, 2)
  AND CONCEPTOS.CLASE           IN (8)
  AND HISTORICOS.ID_DE_CONCEPTO BETWEEN '490' AND '499' )
  GROUP BY HISTORICOS.COMPANIA,
    PERSONAL.DEPENDENCIA,
    DEPENDENCIA.NOMBRE,
    CARGOS.NOMBRE_DEL_CARGO,
    PERSONAL.ID_CENTRO_DE_COSTO,
    CENTRO_COSTO.NOMBRE,
    PERSONAL.APELLIDO1
    || ' '
    || PERSONAL.APELLIDO2
    || ' '
    || PERSONAL.NOMBRES,
    PERSONAL.ID_DE_EMPLEADO,
    PERSONAL.CMACMO
  ORDER BY PERSONAL.DEPENDENCIA,
    PERSONAL.ID_CENTRO_DE_COSTO,
    PERSONAL.APELLIDO1
    || ' '
    || PERSONAL.APELLIDO2
    || ' '
    || PERSONAL.NOMBRES
  )
SELECT CRA688_MESANT.COMPANIA,
  CRA688_MESANT.DEPENDENCIA,
  CRA688_MESANT.NOMBREDEPENDENCIA,
  CRA688_MESANT.NOMBRE_DEL_CARGO,
  CRA688_MESANT.ID_CENTRO_DE_COSTO,
  CRA688_MESANT.NOMBRE,
  CRA688_MESANT.NOMBRECOMPLETO,
  NVL(CRA688_MESANT.TOTALMESANT,0)       AS TOTALMESANT,
  NVL(CRA688_MESACTUAL.TOTALMESACTUAL,0) AS TOTALMESACTUAL,
  CASE
    WHEN CRA688_MESANT.CMACMO IS NOT NULL
    THEN CRA688_MESANT.CMACMO
    WHEN CRA688_MESACTUAL.CMACMO IS NULL
    THEN '2'
    ELSE '1'
  END AS CMACMO,
  CATEGORIA.SALARIO_BASE
FROM CRA688_MESANT
LEFT JOIN CRA688_MESACTUAL
ON CRA688_MESANT.COMPANIA            = CRA688_MESACTUAL.COMPANIA
AND CRA688_MESANT.DEPENDENCIA        = CRA688_MESACTUAL.DEPENDENCIA
AND CRA688_MESANT.ID_CENTRO_DE_COSTO = CRA688_MESACTUAL.ID_CENTRO_DE_COSTO
AND CRA688_MESANT.ID_DE_EMPLEADO     = CRA688_MESACTUAL.ID_DE_EMPLEADO
INNER JOIN PERSONAL
ON CRA688_MESANT.COMPANIA        = PERSONAL.COMPANIA
AND CRA688_MESANT.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
INNER JOIN CATEGORIA
ON PERSONAL.COMPANIA         = CATEGORIA.COMPANIA
AND PERSONAL.ESCALAFON       = CATEGORIA.ESCALAFON
AND PERSONAL.ID_DE_CATEGORIA = CATEGORIA.ID_DE_CATEGORIA
AND PERSONAL.ANO             = CATEGORIA.ANO
ORDER BY CRA688_MESANT.DEPENDENCIA,
  CRA688_MESANT.ID_CENTRO_DE_COSTO
