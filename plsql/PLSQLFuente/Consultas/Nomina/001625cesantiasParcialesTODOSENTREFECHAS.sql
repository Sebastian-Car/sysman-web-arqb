SELECT CESANTIAS.ID_DE_EMPLEADO,
  CESANTIAS.FECHA,
  CESANTIAS.CESANTIA,
  CESANTIAS.INTERES,
  TRIM(PERSONAL.APELLIDO1)
  ||' '
  || TRIM(PERSONAL.APELLIDO2)
  ||' '
  || TRIM(PERSONAL.NOMBRES) AS NOMBRECOMPLETO,
  CESANTIAS.COMPANIA,
  DECODE(CESANTIAS.CONSIGNADAS,-1,'Si','No')CONSIGNADAS,
  CESANTIAS.NUMRESOLUCION,
  CESANTIAS.FECHARESOLUCION
FROM CESANTIAS
LEFT JOIN PERSONAL
ON CESANTIAS.ID_DE_EMPLEADO = PERSONAL.ID_DE_EMPLEADO
AND CESANTIAS.COMPANIA      = PERSONAL.COMPANIA
WHERE CESANTIAS.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
AND CESANTIAS.COMPANIA='s$compania$s'
ORDER BY CESANTIAS.ID_DE_EMPLEADO,
  CESANTIAS.FECHA 
