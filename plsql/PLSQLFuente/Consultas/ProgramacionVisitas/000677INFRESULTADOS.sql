SELECT VISITA.CODIGO, VISITA.EXPEDIENTE, VISITA.FECHA_INICIO, VISITA.FECHA_FIN, VISITA.BARRIO, 
       VISITA.DIRECCION,CLASE_VISITA.NOMBRE As CLASE_VISITA, RESULTADO_VISITA.NOMBRE As RESULTADO
FROM   VISITA INNER JOIN CLASE_VISITA 
       ON  (VISITA.COMPANIA = CLASE_VISITA.COMPANIA) 
       AND (VISITA.CLASE = CLASE_VISITA.CODIGO)
	   LEFT JOIN RESULTADO_VISITA
	   ON  (VISITA.COMPANIA = RESULTADO_VISITA.COMPANIA)
	   AND (VISITA.RESULTADO = RESULTADO_VISITA.CODIGO)
       LEFT JOIN ESTADO_VISITA
       ON  (VISITA.COMPANIA = ESTADO_VISITA.COMPANIA)
       AND (VISITA.ESTADO = ESTADO_VISITA.CODIGO)
WHERE  VISITA.COMPANIA = s$compania$s
AND    VISITA.CLASE = s$clase$s
AND    VISITA.CODIGO = s$codigo$s
AND    VISITA.NUMERO = s$numero$s
