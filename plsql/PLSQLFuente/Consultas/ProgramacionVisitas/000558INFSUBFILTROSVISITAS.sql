SELECT  RESPONSABLE_VISITA.COMPANIA, 
        RESPONSABLE_VISITA.CLASE_VISITA, 
        RESPONSABLE_VISITA.CODIGO_VISITA, 
        RESPONSABLE_VISITA.NUMERO_VISITA, 
        RESPONSABLE_VISITA.DEPENDENCIA, 
        RESPONSABLE_VISITA.RESPONSABLE, 
        TERCERO.NOMBRE AS NOMRESPONSABLE, 
        RESPONSABLE.CARGO, 
        DEPENDENCIA.NOMBRE AS NOMDEPENDENCIA
 FROM RESPONSABLE_VISITA 
  INNER JOIN RESPONSABLE 
    ON RESPONSABLE_VISITA.RESPONSABLE = RESPONSABLE.CEDULA 
    AND RESPONSABLE_VISITA.COMPANIA = RESPONSABLE.COMPANIA 
  INNER JOIN DEPENDENCIA 
    ON RESPONSABLE_VISITA.DEPENDENCIA = DEPENDENCIA.CODIGO 
    AND RESPONSABLE_VISITA.COMPANIA = DEPENDENCIA.COMPANIA
  INNER JOIN TERCERO
    ON RESPONSABLE.COMPANIA = TERCERO.COMPANIA
    AND RESPONSABLE.CEDULA = TERCERO.NIT
    AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
 WHERE  RESPONSABLE_VISITA.COMPANIA = $P{PR_COMPANIA} 
    AND RESPONSABLE_VISITA.CLASE_VISITA = $P{PR_CLASE_VISITA}  
    AND  RESPONSABLE_VISITA.CODIGO_VISITA =  $P{PR_CODIGO_VISITA}   
    AND  RESPONSABLE_VISITA.NUMERO_VISITA = $P{PR_NUMERO_VISITA}
