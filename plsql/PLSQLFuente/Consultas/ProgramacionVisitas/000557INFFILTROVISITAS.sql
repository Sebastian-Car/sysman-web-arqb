SELECT  DISTINCT CLASE_VISITA.NOMBRE AS CLASEVISITA, 
        VISITA.CODIGO AS CODIGOVISITA, 
        VISITA.NUMERO AS NROVISITA, 
        VISITA.FECHA_INICIO, 
        VISITA.FECHA_FIN, 
        ESTADO_VISITA.NOMBRE AS ESTADOVISITA, 
        VISITA.BARRIO, 
        VISITA.DIRECCION, 
        VISITA.MATRICULA_MERCANTIL, 
        VISITA.EXPEDIENTE, 
        VISITA.DESCIPCION, 
        RESULTADO_VISITA.NOMBRE AS RESULTADOVISITA, 
        VISITA.OBSERVACIONES, 
        VISITA.COMPANIA, 
        VISITA.CLASE, 
        VISITA.CODIGO, 
        VISITA.ESTADO
FROM VISITA 
  INNER JOIN CLASE_VISITA 
    ON VISITA.COMPANIA = CLASE_VISITA.COMPANIA 
    AND VISITA.CLASE = CLASE_VISITA.CODIGO 
  INNER JOIN ESTADO_VISITA 
    ON VISITA.COMPANIA = ESTADO_VISITA.COMPANIA 
    AND VISITA.ESTADO = ESTADO_VISITA.CODIGO 
  LEFT JOIN RESULTADO_VISITA 
    ON VISITA.COMPANIA = RESULTADO_VISITA.COMPANIA 
    AND VISITA.RESULTADO = RESULTADO_VISITA.CODIGO
  
WHERE VISITA.COMPANIA = s$compania$s  
