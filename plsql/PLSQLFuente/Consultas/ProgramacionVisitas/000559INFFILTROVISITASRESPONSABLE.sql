SELECT DISTINCT CLASE_VISITA.NOMBRE AS CLASEVISITA, 
       VISITA.CODIGO AS CODIGOVISITA, 
       VISITA.NUMERO AS NROVISITA, 
       VISITA.FECHA_INICIO, 
       VISITA.FECHA_FIN, 
       ESTADO_VISITA.NOMBRE AS ESTADOVISITA, 
       VISITA.BARRIO, 
       VISITA.DIRECCION, 
       VISITA.MATRICULA_MERCANTIL, 
       VISITA.EXPEDIENTE, 
       VISITA.DESCIPCION, 
       RESULTADO_VISITA.NOMBRE AS RESULTADOVISITA, 
       VISITA.OBSERVACIONES, 
       VISITA.COMPANIA, 
       VISITA.CLASE, 
       VISITA.CODIGO, 
       VISITA.ESTADO, 
       RESPONSABLE_VISITA.RESPONSABLE,
       TERCERO.NOMBRE
FROM VISITA 
  INNER JOIN CLASE_VISITA 
    ON VISITA.CLASE = CLASE_VISITA.CODIGO 
    AND VISITA.COMPANIA = CLASE_VISITA.COMPANIA 
  INNER JOIN ESTADO_VISITA 
    ON VISITA.ESTADO = ESTADO_VISITA.CODIGO 
    AND VISITA.COMPANIA = ESTADO_VISITA.COMPANIA 
  LEFT JOIN RESULTADO_VISITA 
    ON VISITA.RESULTADO = RESULTADO_VISITA.CODIGO 
    AND VISITA.COMPANIA = RESULTADO_VISITA.COMPANIA 
  LEFT JOIN RESPONSABLE_VISITA 
    ON VISITA.NUMERO = RESPONSABLE_VISITA.NUMERO_VISITA 
    AND VISITA.CODIGO = RESPONSABLE_VISITA.CODIGO_VISITA 
    AND VISITA.CLASE = RESPONSABLE_VISITA.CLASE_VISITA 
    AND VISITA.COMPANIA = RESPONSABLE_VISITA.COMPANIA
   LEFT JOIN RESPONSABLE 
    ON RESPONSABLE_VISITA.RESPONSABLE = RESPONSABLE.CEDULA 
    AND RESPONSABLE_VISITA.COMPANIA = RESPONSABLE.COMPANIA 
    LEFT JOIN TERCERO
    ON RESPONSABLE.COMPANIA = TERCERO.COMPANIA
    AND RESPONSABLE.CEDULA = TERCERO.NIT
    AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
  WHERE VISITA.COMPANIA = s$compania$s
