MERGE INTO CONSULTAS FIN USING (SELECT '000253InformeVigenciaPolizas' INFORME ,TO_CLOB(q'[SELECT ORDENDECOMPRA.COMPANIA,
  ORDENDECOMPRA.CLASEORDEN,
  ORDENDECOMPRA.NUMERO,
  ORDENDECOMPRA.NOMBRECONTRATISTA,
  TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') FECHA,
  ORDENDECOMPRA.DURACION,
  ORDENDECOMPRA.VALORTOTAL,
  TO_CHAR(POLIZAS.FECHAEXPEDICION,'DD/MM/YYYY') FECHAEXPEDICION,
  TO_CHAR(POLIZAS.VIGENCIADESDE,'DD/MM/YYYY') VIGENCIADESDE,
  TO_CHAR(POLIZAS.VIGENCIAHASTA,'DD/MM/YYYY') VIGENCIAHASTA,
  TRUNC(POLIZAS.VIGENCIAHASTA) - TRUNC(POLIZAS.VIGENCIADESDE) DURACIONPOLIZA,
  CASE
    WHEN SYSDATE < POLIZAS.VIGENCIAHASTA
    THEN TRUNC(POLIZAS.VIGENCIAHASTA) - TRUNC(SYSDATE)
    ELSE 0
  END VENCIMIENTOPOLIZA,
  POLIZAS.VALORASEGURADO,
  TIPOPOLIZA.DESCRIPCION,
  (POLIZAS.CODIGO
  || POLIZAS.CONSECUTIVO) CODIGOPOLIZA
FROM POLIZAS
INNER JOIN ORDENDECOMPRA
ON POLIZAS.COMPANIA       = ORDENDECOMPRA.COMPANIA
AND POLIZAS.CLASEORDEN    = ORDENDECOMPRA.CLASEORDEN
AND POLIZAS.ORDENDECOMPRA = ORDENDECOMPRA.NUMERO
INNER JOIN ASEGURADORA
ON ASEGURADORA.COMPANIA        = POLIZAS.COMPANIA
AND ASEGURADORA.NITASEGURADORA = POLIZAS.ASEGURADORA
AND ASEGURADORA.SUCURSAL       = POLIZAS.SUCURSAL
INNER JOIN TIPOPOLIZA
ON TIPOPOLIZA.CODIGO         = POLIZAS.TIPO
WHERE ORDENDECOMPRA.COMPANIA = s$compania$s
AND ORDENDECOMPRA.CLASEORDEN BETWEEN 's$tipoIni$s' AND 's$tipoFin$s' 
AND ORDENDECOMPRA.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s]') CONSULTA, 9 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);