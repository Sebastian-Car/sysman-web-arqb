SELECT ORDENDECOMPRA.NUMERO,
       TERCERO.NOMBRE, 
       ORDENDECOMPRA.TERCERO,
       ORDENDECOMPRA.NUMEROINMUEBLE,
       ORDENDECOMPRA.DESCRIPCIONINMUEBLE,
       ORDENDECOMPRA.CANONINCLUIDOIVA,
       TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') FECHA,
       TO_CHAR(ORDENDECOMPRA.FECHAFINALIZACION,'DD/MM/YYYY') FECHAFINALIZACION,
       ORDENDECOMPRA.DIRECCIONINMUEBLE,
       ORDENDECOMPRA.OBSERVACIONESINMUEBLE,
       PCK_CONTRATOS.FC_FECHAPOLIZA(ORDENDECOMPRA.CLASEORDEN, ORDENDECOMPRA.NUMERO,1,ORDENDECOMPRA.COMPANIA) FECHAINICIO,
       PCK_CONTRATOS.FC_FECHAPOLIZA(ORDENDECOMPRA.CLASEORDEN, ORDENDECOMPRA.NUMERO,2,ORDENDECOMPRA.COMPANIA) FECHAVENCIMIENTO,
       PCK_CONTRATOS.FC_ESTADOPOLIZA(ORDENDECOMPRA.CLASEORDEN, ORDENDECOMPRA.NUMERO,ORDENDECOMPRA.COMPANIA) POLIZAS
FROM ORDENDECOMPRA 
	LEFT JOIN TERCERO 
	       ON ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA 
	      AND ORDENDECOMPRA.TERCERO  = TERCERO.NIT 
	      AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL 
    LEFT JOIN POLIZAS 
           ON ORDENDECOMPRA.COMPANIA   = POLIZAS.COMPANIA 
          AND ORDENDECOMPRA.CLASEORDEN = POLIZAS.CLASEORDEN 
          AND ORDENDECOMPRA.NUMERO     = POLIZAS.ORDENDECOMPRA 
    LEFT JOIN TIPOORDENDECOMPRA 
           ON ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO 
          AND ORDENDECOMPRA.COMPANIA   = TIPOORDENDECOMPRA.COMPANIA
WHERE ORDENDECOMPRA.COMPANIA   =s$compania$s
  AND ORDENDECOMPRA.CLASEORDEN ='CDA'
  AND NVL(TIPOORDENDECOMPRA.CLASE,'') <>'M'