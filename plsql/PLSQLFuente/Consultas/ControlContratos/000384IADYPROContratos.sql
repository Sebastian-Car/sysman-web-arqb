SELECT 
ORDENDECOMPRA.TIPOAFECTADO,
TIPOORDENDECOMPRAAFECT.NOMBRE NOMBREAFECTADO,
TIPOORDENDECOMPRA.NOMBRE TIPONOMBRE,
  ORDENDECOMPRA.NUMEROAFECTADO ORDENNUMERO,
  TERCERO.NOMBRE TERCERONOMBRE,
  TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') ORDENFECHA,
  ORDENDECOMPRA.CLASEORDEN TIPOT,
  TIPOORDENDECOMPRA.NOMBRE CLASENOMBRE,
  TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') NOVEDADFECHA,
  ORDENDECOMPRA.NUMERO NOVEDADNUMERO,
  ORDENDECOMPRA.VALORTOTAL VALORTOTAL,
  ORDENDECOMPRA.VALORTOTAL ORDENVALORTOTAL
FROM ORDENDECOMPRA
LEFT JOIN TERCERO
ON ORDENDECOMPRA.COMPANIA  = TERCERO.COMPANIA
AND ORDENDECOMPRA.TERCERO  = TERCERO.NIT
AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL
LEFT JOIN TIPOORDENDECOMPRA
ON ORDENDECOMPRA.COMPANIA    = TIPOORDENDECOMPRA.COMPANIA
AND ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO
LEFT JOIN TIPOORDENDECOMPRA TIPOORDENDECOMPRAAFECT
ON ORDENDECOMPRA.COMPANIA    = TIPOORDENDECOMPRAAFECT.COMPANIA
AND ORDENDECOMPRA.TIPOAFECTADO = TIPOORDENDECOMPRAAFECT.CODIGO
LEFT JOIN CLASENOVEDAD
ON ORDENDECOMPRA.CLASENOVEDAD = CLASENOVEDAD.CODIGO
WHERE ORDENDECOMPRA.COMPANIA = s$compania$s
AND ORDENDECOMPRA.TIPOAFECTADO = 's$claseorden$s'
AND ORDENDECOMPRA.NUMEROAFECTADO BETWEEN  s$numeroinicial$s AND s$numerofinal$s
AND ORDENDECOMPRA.CLASENOVEDAD  IN('D','O')
AND NVL(TIPOORDENDECOMPRA.CLASE,'') = 'M'
ORDER BY ORDENDECOMPRA.COMPANIA,
  ORDENDECOMPRA.CLASEORDEN,
  ORDENDECOMPRA.NUMERO
