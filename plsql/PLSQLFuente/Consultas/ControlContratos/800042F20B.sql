WITH ADICION_CONTRATOS AS(
SELECT ORDENDECOMPRA.COMPANIA, 
       ORDENDECOMPRA.TIPOAFECTADO AS CLASEORDEN, 
       ORDENDECOMPRA.NUMEROAFECTADO AS ORDENDECOMPRA,
       ORDENDECOMPRA.VALORTOTAL AS VALORADICION
FROM ORDENDECOMPRA 
INNER JOIN TIPOORDENDECOMPRA ON 
ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA AND 
ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO
WHERE TIPOORDENDECOMPRA.CLASE='M' 
  AND ORDENDECOMPRA.COMPANIA= s$compania$s 
  AND EXTRACT (YEAR FROM FECHA)= EXTRACT (YEAR FROM SYSDATE)
GROUP BY ORDENDECOMPRA.COMPANIA,
ORDENDECOMPRA.TIPOAFECTADO, 
ORDENDECOMPRA.NUMEROAFECTADO,
ORDENDECOMPRA.VALORTOTAL
UNION ALL
SELECT NOVEDADCONTRATO.COMPANIA, 
       NOVEDADCONTRATO.CLASEORDEN,
       NOVEDADCONTRATO.ORDENDECOMPRA, 
       SUM(NOVEDADCONTRATO.VALORTOTAL) AS VALORADICION
FROM NOVEDADCONTRATO 
WHERE NOVEDADCONTRATO.CLASET = 'D'
GROUP BY NOVEDADCONTRATO.COMPANIA,
       NOVEDADCONTRATO.CLASEORDEN, 
       NOVEDADCONTRATO.ORDENDECOMPRA, 
       NOVEDADCONTRATO.CLASET
)

SELECT ORDENDECOMPRA.COMPANIA,
       ORDENDECOMPRA.NUMERO, 
       NVL(VALORADICION,0) AS VLRADICION,
       CASE  WHEN ADICION_CONTRATOS.CLASEORDEN IS NULL THEN 'NO' ELSE 'SI' END AS PRORROGA, 
       'ND' AS SECOP, 
       CASE WHEN ORDENDECOMPRA.FECHALIQUIDACION IS NULL THEN 'ND' ELSE TO_CHAR(ORDENDECOMPRA.FECHALIQUIDACION,'YYYY/MM/DD') END AS FECHALIQUIDA
FROM ORDENDECOMPRA LEFT JOIN ADICION_CONTRATOS ON
       ORDENDECOMPRA.NUMERO = ADICION_CONTRATOS.ORDENDECOMPRA AND 
       ORDENDECOMPRA.CLASEORDEN = ADICION_CONTRATOS.CLASEORDEN AND 
       ORDENDECOMPRA.COMPANIA = ADICION_CONTRATOS.COMPANIA
WHERE ORDENDECOMPRA.COMPANIA= s$compania$s
  AND EXTRACT (YEAR FROM FECHA)= EXTRACT (YEAR FROM SYSDATE)
  AND EXTRACT (MONTH FROM FECHA) BETWEEN s$mesInicial$s AND s$mesFinal$s
