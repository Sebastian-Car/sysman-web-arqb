SELECT  ADS_ADI_NOVEDADES_PRUEBA.COMPANIA,
        ADS_ADI_NOVEDADES_PRUEBA.NUMERO,
        TO_CHAR(ADS_ADI_NOVEDADES_PRUEBA.FECHA,'DD/MM/YYYY')  FECHA,
        UPPER(PCK_SYSMAN_UTL.FC_NOMBRE_MES(TO_CHAR(ADS_ADI_NOVEDADES_PRUEBA.FECHA,'MM'))) MES,
        TO_CHAR(ADS_ADI_NOVEDADES_PRUEBA.FECHA,'YYYY') ANIO,
        ADS_ADI_NOVEDADES_PRUEBA.CLASEORDEN,
        ADS_ADI_NOVEDADES_PRUEBA.NOMBRETERCERO, 
        ADS_ADI_NOVEDADES_PRUEBA.NIT, 
        SUBSTR(ORDENDECOMPRA.DESCRIPCION,1,255) DESCRIPCIONF,
        ADS_ADI_NOVEDADES_PRUEBA.VALORTOTAL, 
        NOVEDADCONTRATO.VALOR_LIBERADO, 
        ADS_ADI_NOVEDADES_PRUEBA.PLAZODEENTREGA, 
        TO_CHAR(ADS_ADI_NOVEDADES_PRUEBA.FECHAINICIO,'DD/MM/YYYY') FECHAINICIO, 
        NOVEDADCONTRATO.TIPOT NOVEDADTIPOT,
        TO_CHAR(ADS_ADI_NOVEDADES_PRUEBA.FECHAFINALIZACION,'DD/MM/YYYY') FECHAFINALIZACION, 
        ADS_ADI_NOVEDADES_PRUEBA.HONORARIOS, 
        ADS_ADI_NOVEDADES_PRUEBA.TOPEVEHICULO, 
        ADS_ADI_NOVEDADES_PRUEBA.APORTESTE  
FROM ORDENDECOMPRA 
INNER JOIN (SELECT TIPOORDENDECOMPRA.COMPANIA, 
              ORDENDECOMPRA.TIPOAFECTADO CLASEORDEN, 
              TERCERO.NOMBRE NOMBRETERCERO, 
              TERCERO.NIT,
              TIPOORDENDECOMPRA.CODIGO TIPOT, 
              ORDENDECOMPRA.FECHA, 
              ORDENDECOMPRA.NUMERO NOVEDAD, 
              ORDENDECOMPRA_1.NUMERO,
              ORDENDECOMPRA_1.FECHAINICIO, 
              ORDENDECOMPRA_1.PLAZODEENTREGA, 
              ORDENDECOMPRA_1.VALORTOTAL, 
              ORDENDECOMPRA_1.HONORARIOS, 
              ORDENDECOMPRA_1.TOPEVEHICULO, 
              ORDENDECOMPRA_1.FECHAFINALIZACION, 
              ORDENDECOMPRA_1.APORTESTE
        FROM TIPOORDENDECOMPRA 
          LEFT JOIN ORDENDECOMPRA 
              ON TIPOORDENDECOMPRA.COMPANIA = ORDENDECOMPRA.COMPANIA 
              AND TIPOORDENDECOMPRA.CODIGO = ORDENDECOMPRA.CLASEORDEN 
          LEFT JOIN TERCERO 
              ON ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA 
              AND ORDENDECOMPRA.TERCERO = TERCERO.NIT 
              AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL
          LEFT JOIN ORDENDECOMPRA ORDENDECOMPRA_1 
              ON  ORDENDECOMPRA.COMPANIA = ORDENDECOMPRA_1.COMPANIA 
              AND ORDENDECOMPRA.TIPOAFECTADO = ORDENDECOMPRA_1.CLASEORDEN 
              AND ORDENDECOMPRA.NUMEROAFECTADO = ORDENDECOMPRA_1.NUMERO 
        WHERE TIPOORDENDECOMPRA.COMPANIA = s$compania$s
        AND ORDENDECOMPRA.TIPOAFECTADO = 's$tipoContrato$s' 
        AND ORDENDECOMPRA.FECHA BETWEEN  s$fechaInicial$s AND s$fechaFinal$s
        AND ORDENDECOMPRA.CLASENOVEDAD = 'D' 
        AND TIPOORDENDECOMPRA.CLASE = 'M'
        AND ORDENDECOMPRA.CLASEORDEN = 's$tipon$s'
          UNION 
        SELECT  ORDENDECOMPRA.COMPANIA, 
                NOVEDADCONTRATO.CLASEORDEN, 
                TERCERO.NOMBRE NOMBRETERCERO,
                TERCERO.NIT,
                NOVEDADCONTRATO.TIPOT,
                NOVEDADCONTRATO.FECHA,
                ORDENDECOMPRA.NUMERO,
                NOVEDADCONTRATO.NOVEDAD, 
                ORDENDECOMPRA.FECHAINICIO, 
                ORDENDECOMPRA.PLAZODEENTREGA, 
                ORDENDECOMPRA.VALORTOTAL, 
                ORDENDECOMPRA.HONORARIOS, 
                ORDENDECOMPRA.TOPEVEHICULO, 
                ORDENDECOMPRA.FECHAFINALIZACION,
                ORDENDECOMPRA.APORTESTE
          FROM ORDENDECOMPRA 
              LEFT JOIN TIPOORDENDECOMPRA 
                  ON ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA 
                  AND ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO  
              LEFT JOIN TERCERO 
                  ON ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA  
                  AND ORDENDECOMPRA.TERCERO = TERCERO.NIT 
                  AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL
              LEFT JOIN NOVEDADCONTRATO 
                  ON ORDENDECOMPRA.COMPANIA = NOVEDADCONTRATO.COMPANIA  
                  AND ORDENDECOMPRA.CLASEORDEN = NOVEDADCONTRATO.CLASEORDEN 
                  AND ORDENDECOMPRA.NUMERO = NOVEDADCONTRATO.ORDENDECOMPRA 
          WHERE ORDENDECOMPRA.COMPANIA = s$compania$s
         AND NOVEDADCONTRATO.CLASEORDEN = 's$tipoContrato$s'
         AND NOVEDADCONTRATO.TIPOT = 's$tipon$s' 
         AND NOVEDADCONTRATO.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
          ) ADS_ADI_NOVEDADES_PRUEBA 
ON ORDENDECOMPRA.COMPANIA     = ADS_ADI_NOVEDADES_PRUEBA.COMPANIA 
AND ORDENDECOMPRA.CLASEORDEN  = ADS_ADI_NOVEDADES_PRUEBA.CLASEORDEN 
AND ORDENDECOMPRA.NUMERO      = ADS_ADI_NOVEDADES_PRUEBA.NOVEDAD 
INNER JOIN NOVEDADCONTRATO 
ON  ORDENDECOMPRA.COMPANIA    = NOVEDADCONTRATO.COMPANIA 
AND ORDENDECOMPRA.CLASEORDEN  = NOVEDADCONTRATO.CLASEORDEN 
AND ORDENDECOMPRA.NUMERO       = NOVEDADCONTRATO.ORDENDECOMPRA 
WHERE NOVEDADCONTRATO.TIPOT = 'ACL'
GROUP BY ADS_ADI_NOVEDADES_PRUEBA.COMPANIA,
ADS_ADI_NOVEDADES_PRUEBA.NUMERO,
ADS_ADI_NOVEDADES_PRUEBA.FECHA, 
ADS_ADI_NOVEDADES_PRUEBA.CLASEORDEN,
ADS_ADI_NOVEDADES_PRUEBA.NOMBRETERCERO, 
ADS_ADI_NOVEDADES_PRUEBA.NIT, 
SUBSTR(ORDENDECOMPRA.DESCRIPCION,1,255) ,
ADS_ADI_NOVEDADES_PRUEBA.VALORTOTAL, 
NOVEDADCONTRATO.VALOR_LIBERADO, 
ADS_ADI_NOVEDADES_PRUEBA.PLAZODEENTREGA, 
ADS_ADI_NOVEDADES_PRUEBA.FECHAINICIO,
NOVEDADCONTRATO.TIPOT,
ADS_ADI_NOVEDADES_PRUEBA.FECHAFINALIZACION, 
ADS_ADI_NOVEDADES_PRUEBA.HONORARIOS, 
ADS_ADI_NOVEDADES_PRUEBA.TOPEVEHICULO, 
ADS_ADI_NOVEDADES_PRUEBA.APORTESTE
