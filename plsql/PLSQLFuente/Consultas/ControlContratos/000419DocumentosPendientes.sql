SELECT  DOCUMENTOS_CONTRATOS.NOMBRE
FROM DOCUMENTOS 
        INNER JOIN DOCUMENTOS_CONTRATOS 
            ON DOCUMENTOS.DOCUMENTO = DOCUMENTOS_CONTRATOS.CODIGO 
WHERE DOCUMENTOS.COMPANIA   = s$compania$s 
  AND DOCUMENTOS.CLASEORDEN = 's$claseOrden$s' 
  AND DOCUMENTOS.NUMERO     = s$numeroOrden$s 
  AND DOCUMENTOS.ENTREGADO  IN(0)
