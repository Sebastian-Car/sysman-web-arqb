SELECT 
  ORDENDECOMPRA.COMPANIA,
  ORDENDECOMPRA.CLASEORDEN,
  ORDENDECOMPRA.NUMERO,
  TERCERO.NOMBRE,
  ORDENDECOMPRA.OBJETOCONTRATO,
  ORDENDECOMPRA.VALORTOTAL,
  ORDENDECOMPRA.INTERVENTOR,
  NVL(C_ADICIONESCONTRATOEJEC.ADICIONES,0)+NVL(ORDENDECOMPRA.ADICIONES,0) ADICIONESACONTRATO,
  CASE WHEN NVL(ORDENDECOMPRA.VALORTOTAL,0)=NVL(PAGOANOANTERIOR,0) THEN 0 ELSE NVL(ORDENDECOMPRA.VALORTOTAL,0)-(NVL(PAGOPERIODOANTERIOR,0)+NVL(PAGOPERIODOACTUAL,0)) END SALDOCONTRATO,
  NVL(PAGOANOANTERIOR,0)+NVL(ABONOS,0) PAGOANTERIOR,
  NVL(PAGOPERIODOANTERIOR,0) PAGOPERIODOANTERIORN,
  NVL(PAGOPERIODOACTUAL,0) PAGOPERIODOACTUALN,
  NVL(ABONOS,0)+NVL(PAGOANOANTERIOR,0)+NVL(PAGOPERIODOANTERIOR,0)+NVL(PAGOPERIODOACTUAL,0) TOTALPAGADO,
  ROUND((NVL(ABONOS,0)+NVL(ORDENDECOMPRA.VALORPAGOS,0)),0)*100/NVL((CASE WHEN ORDENDECOMPRA.VALORTOTAL=0 THEN 1 ELSE ORDENDECOMPRA.VALORTOTAL END),1) AS PORCEJECUCION,
  TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') FECHA,
  TO_CHAR(ORDENDECOMPRA.FECHAFINALIZACION,'DD/MM/YYYY') FECHAFINALIZACION,
  CASE WHEN MANEJA = 'E' THEN 'X' ELSE ' ' END  BIENES,
  CASE WHEN MANEJA <> 'E' THEN 'X' ELSE ' ' END  SERVICIOS,
  TO_CHAR(FECHAFINALIZACION,'YYYYMMDD') EXPR2,
  ORDENDECOMPRA.VALORPAGOS,
  C_PAGOSCONTRATOEJEC.VALORPAGOS VALORPAGOSREAL,
  VALORTOTAL-C_PAGOSCONTRATOEJEC.VALORPAGOS SALDOTOTAL_
FROM  ORDENDECOMPRA LEFT JOIN TIPOORDENDECOMPRA 
        ON  ORDENDECOMPRA.COMPANIA    =   TIPOORDENDECOMPRA.COMPANIA
        AND ORDENDECOMPRA.CLASEORDEN  =   TIPOORDENDECOMPRA.CODIGO 
      LEFT JOIN ( SELECT 
                    COMPROBANTE_CNT.COMPANIA,
                    SUM(CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='P' THEN NVL(VLR_DOCUMENTO,0) ELSE 0 END) VALORPAGOS1,
                    SUM(CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='A' THEN NVL(VLR_DOCUMENTO,0) ELSE 0 END) VALORPAGOS2,
                    NVL((SUM(CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='P' THEN NVL(VLR_DOCUMENTO,0) ELSE 0 END)),0)-NVL((SUM(CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='A' THEN NVL(VLR_DOCUMENTO,0) ELSE 0 END)),0) VALORPAGOS,
                    COMPROBANTE_CNT.NUMEROCONTRATO,
                    COMPROBANTE_CNT.TIPOCONTRATO,
                    SUM(CASE WHEN COMPROBANTE_CNT.ANO<EXTRACT (YEAR FROM TO_DATE('s$desde$s','dd/MM/yyyy')) THEN (CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='E' THEN NVL(VLR_DOCUMENTO,0) ELSE 0 END) ELSE (CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='E' THEN  NVL(VLR_DOCUMENTO,0) ELSE 0 END) END) PAGOANOANTERIORE,
                    SUM(CASE WHEN COMPROBANTE_CNT.ANO<EXTRACT (YEAR FROM TO_DATE('s$desde$s','dd/MM/yyyy')) THEN (CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='A' THEN NVL(VLR_DOCUMENTO,0) ELSE 0 END) ELSE (CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='A' THEN  NVL(VLR_DOCUMENTO,0) ELSE 0 END) END) PAGOANOANTERIORCHA,
                    SUM(CASE WHEN (FECHA<TO_DATE('s$desde$s','dd/MM/yyyy') AND COMPROBANTE_CNT.ANO=EXTRACT (YEAR FROM TO_DATE('s$desde$s','dd/MM/yyyy'))) THEN (CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='E' THEN NVL(VLR_DOCUMENTO,0) ELSE 0 END) ELSE 0 END) PAGOPERIODOANTERIORE,
                    SUM(CASE WHEN (FECHA<TO_DATE('s$desde$s','dd/MM/yyyy') AND COMPROBANTE_CNT.ANO=EXTRACT (YEAR FROM TO_DATE('s$desde$s','dd/MM/yyyy'))) THEN (CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='A' THEN NVL(VLR_DOCUMENTO,0) ELSE 0 END) ELSE 0 END) PAGOPERIODOANTERIORCHA,
                    SUM(CASE WHEN FECHA=TO_DATE('s$desde$s','dd/MM/yyyy') THEN (CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='E' THEN NVL(VLR_DOCUMENTO,0) ELSE 0 END) ELSE 0 END) PAGOPERIODOACTUALE,
                    SUM(CASE WHEN FECHA=TO_DATE('s$desde$s','dd/MM/yyyy') THEN (CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='A' THEN NVL(VLR_DOCUMENTO,0) ELSE 0 END) ELSE 0 END) PAGOPERIODOACTUALCHA,  
                    NVL((SUM(CASE WHEN COMPROBANTE_CNT.ANO<EXTRACT (YEAR FROM TO_DATE('s$desde$s','dd/MM/yyyy')) THEN (CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='E' THEN NVL(VLR_DOCUMENTO,0) ELSE 0 END) ELSE (CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='E' THEN  NVL(VLR_DOCUMENTO,0) ELSE 0 END) END)),0)-NVL((SUM(CASE WHEN COMPROBANTE_CNT.ANO<EXTRACT (YEAR FROM TO_DATE('s$desde$s','dd/MM/yyyy')) THEN (CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='A' THEN NVL(VLR_DOCUMENTO,0) ELSE 0 END) ELSE (CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='A' THEN  NVL(VLR_DOCUMENTO,0) ELSE 0 END) END)),0) PAGOANOANTERIOR,
                    (SUM(CASE WHEN (FECHA<TO_DATE('s$desde$s','dd/MM/yyyy') AND COMPROBANTE_CNT.ANO=EXTRACT (YEAR FROM TO_DATE('s$desde$s','dd/MM/yyyy')))THEN (CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='E' THEN NVL(VLR_DOCUMENTO,0) ELSE 0 END) ELSE 0 END))-(SUM(CASE WHEN (FECHA<TO_DATE('s$desde$s','dd/MM/yyyy') AND COMPROBANTE_CNT.ANO=EXTRACT (YEAR FROM TO_DATE('s$desde$s','dd/MM/yyyy'))) THEN (CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='A' THEN NVL(VLR_DOCUMENTO,0) ELSE 0 END) ELSE 0 END)) PAGOPERIODOANTERIOR,
                    (SUM(CASE WHEN FECHA=TO_DATE('s$desde$s','dd/MM/yyyy') THEN (CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='E' THEN NVL(VLR_DOCUMENTO,0) ELSE 0 END) ELSE 0 END))-(SUM(CASE WHEN FECHA=TO_DATE('s$desde$s','dd/MM/yyyy') THEN (CASE WHEN TIPO_COMPROBANTE.CLASE_CONTABLE='A' THEN NVL(VLR_DOCUMENTO,0) ELSE 0 END) ELSE 0 END)) PAGOPERIODOACTUAL,
                    COMPROBANTE_CNT.IND_ANTICIPO
                  FROM  COMPROBANTE_CNT INNER JOIN TIPO_COMPROBANTE
                    ON  COMPROBANTE_CNT.COMPANIA  = TIPO_COMPROBANTE.COMPANIA
                    AND COMPROBANTE_CNT.TIPO      = TIPO_COMPROBANTE.CODIGO
                  WHERE COMPROBANTE_CNT.COMPANIA=s$compania$s AND (COMPROBANTE_CNT.IND_ANTICIPO=0 OR COMPROBANTE_CNT.IND_ANTICIPO IS NULL)
                  GROUP BY 
                    COMPROBANTE_CNT.COMPANIA,
                    COMPROBANTE_CNT.TIPOCONTRATO,
                    COMPROBANTE_CNT.NUMEROCONTRATO,
                    COMPROBANTE_CNT.IND_ANTICIPO,
                    COMPROBANTE_CNT.NUMEROCONTRATO                 
                ) C_PAGOSCONTRATOEJEC 
        ON  ORDENDECOMPRA.COMPANIA    =   C_PAGOSCONTRATOEJEC.COMPANIA
        AND ORDENDECOMPRA.CLASEORDEN  =   C_PAGOSCONTRATOEJEC.TIPOCONTRATO 
        AND ORDENDECOMPRA.NUMERO      =   C_PAGOSCONTRATOEJEC.NUMEROCONTRATO 
    LEFT JOIN (SELECT 
                  DETALLE_COMPROBANTE_PPTAL.COMPANIA,
                  DETALLE_COMPROBANTE_PPTAL.TIPOCONTRATO,
                  DETALLE_COMPROBANTE_PPTAL.NUMEROCONTRATO,
                  SUM(MODIFICACION_DEBITO-MODIFICACION_CREDITO) AS ADICIONES
                FROM  DETALLE_COMPROBANTE_PPTAL INNER JOIN TIPO_COMPROBPP 
                  ON  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
                  AND DETALLE_COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA
                WHERE FECHA<=TO_DATE('s$desde$s','dd/MM/yyyy')
                GROUP BY DETALLE_COMPROBANTE_PPTAL.COMPANIA, DETALLE_COMPROBANTE_PPTAL.TIPOCONTRATO, DETALLE_COMPROBANTE_PPTAL.NUMEROCONTRATO, TIPO_COMPROBPP.CLASE
              ) C_ADICIONESCONTRATOEJEC 
      ON  ORDENDECOMPRA.COMPANIA    =   C_ADICIONESCONTRATOEJEC.COMPANIA
      AND ORDENDECOMPRA.CLASEORDEN  =   C_ADICIONESCONTRATOEJEC.TIPOCONTRATO 
      AND ORDENDECOMPRA.NUMERO      =   C_ADICIONESCONTRATOEJEC.NUMEROCONTRATO 
  LEFT JOIN TERCERO 
    ON  ORDENDECOMPRA.COMPANIA  =   TERCERO.COMPANIA 
    AND ORDENDECOMPRA.TERCERO   =   TERCERO.NIT 
    AND ORDENDECOMPRA.SUCURSAL  =   TERCERO.SUCURSAL
WHERE ORDENDECOMPRA.COMPANIA=s$compania$s 
  AND FECHAFINALIZACION BETWEEN TO_DATE('s$desde$s','dd/MM/yyyy') AND TO_DATE('s$hasta$s','dd/MM/yyyy')
  AND NVL(TIPOORDENDECOMPRA.CLASE,'')<>'M' 
  AND ORDENDECOMPRA.ESTADO<>'A'
ORDER BY 
  ORDENDECOMPRA.COMPANIA,
  ORDENDECOMPRA.CLASEORDEN,
  ORDENDECOMPRA.NUMERO