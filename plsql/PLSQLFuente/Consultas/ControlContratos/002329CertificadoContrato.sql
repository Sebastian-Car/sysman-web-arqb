  ORDENDECOMPRA.NUMERO as NUMERO, 
  ORDENDECOMPRA.CLASEORDEN AS CLASEORDEN,
  ORDENDECOMPRA.OBJETOCONTRATO AS OBJETO,
  ORDENDECOMPRA.OBLIGACIONC AS ACTIVIDAD,
  ORDENDECOMPRA.PLAZODEENTREGA AS PLAZO,
  ORDENDECOMPRA.VALORTOTAL AS VALORCONTRATO, 
  TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') FECHAINICIO,
  TO_CHAR(ORDENDECOMPRA.FECHAFINALIZACION,'DD/MM/YYYY') FECHAFIN ,
  TERCERO.NIT AS CEDULA,
  TERCERO.NOMBRE AS TERCERO,
  NVL(TERCERO.EXPEDIDACEDULA,'-') AS EXPCEDULA
