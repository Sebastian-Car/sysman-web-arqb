WITH TMP_DISPONIBILIDADES AS (SELECT DETALLE_COMPROBANTE_PPTAL.COMPANIA, 
        (CASE WHEN COUNT(DETALLE_COMPROBANTE_PPTAL.COMPANIA) 
                OVER (Partition BY DETALLE_COMPROBANTE_PPTAL.COMPANIA, ORDENDECOMPRA.CLASEORDEN, ORDENDECOMPRAPPTO.NUMERO) >1 
            THEN COUNT(DETALLE_COMPROBANTE_PPTAL.COMPANIA) 
                OVER (Partition BY DETALLE_COMPROBANTE_PPTAL.COMPANIA, ORDENDECOMPRA.CLASEORDEN, ORDENDECOMPRAPPTO.NUMERO)
        ELSE NULL END) CANTIDADRS,
        ORDENDECOMPRA.CLASEORDEN, 
        ORDENDECOMPRAPPTO.NUMERO NUMEROODCPPTO, 
        DETALLE_COMPROBANTE_PPTAL.CUENTA, 
        V_PLAN_PRESUPUESTAL.NOMBRE NCUENTA, 
        TERCERO.NOMBRE CLIENTE, 
        TIPOORDENDECOMPRA.NOMBRE TIPO, 
        ORDENDECOMPRA.CLASEORDEN ||' - '|| TIPOORDENDECOMPRA.NOMBRE TIPOYNOMBRE, 
        ORDENDECOMPRA.CLASEORDEN ||' - '|| ORDENDECOMPRA.NUMERO TIPOYNUMERO, 
        ORDENDECOMPRA.FECHA, 
        SUM(D_ORDENDECOMPRA.VLRTOTAL) VLR_ORDPPTO, 
        SUM(ORDENDECOMPRA.VALORTOTAL) VLR_ORDENC, 
        ORDENDECOMPRAPPTO.NUMEROPPTO, 
        ORDENDECOMPRA.NUMERO NUMEROODC
    FROM (((((DETALLE_COMPROBANTE_PPTAL 
        INNER JOIN ORDENDECOMPRAPPTO 
            ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = ORDENDECOMPRAPPTO.COMPANIA 
                AND DETALLE_COMPROBANTE_PPTAL.ANO = ORDENDECOMPRAPPTO.ANOPPTO 
                AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = ORDENDECOMPRAPPTO.TIPOPPTO 
                AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = ORDENDECOMPRAPPTO.NUMEROPPTO) 
        INNER JOIN ORDENDECOMPRA 
            ON ORDENDECOMPRAPPTO.COMPANIA = ORDENDECOMPRA.COMPANIA 
                AND ORDENDECOMPRAPPTO.CLASEORDEN = ORDENDECOMPRA.CLASEORDEN 
                AND ORDENDECOMPRAPPTO.NUMERO = ORDENDECOMPRA.NUMERO) 
        INNER JOIN TIPOORDENDECOMPRA 
            ON ORDENDECOMPRAPPTO.COMPANIA = TIPOORDENDECOMPRA.COMPANIA 
                AND ORDENDECOMPRAPPTO.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO) 
        INNER JOIN V_PLAN_PRESUPUESTAL 
            ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = V_PLAN_PRESUPUESTAL.COMPANIA 
                AND DETALLE_COMPROBANTE_PPTAL.ANO = V_PLAN_PRESUPUESTAL.ANO 
                AND DETALLE_COMPROBANTE_PPTAL.CUENTA = V_PLAN_PRESUPUESTAL.ID) 
        INNER JOIN TERCERO 
            ON ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA 
                AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL 
                AND ORDENDECOMPRA.TERCERO = TERCERO.NIT)  
        LEFT JOIN D_ORDENDECOMPRA 
            ON ORDENDECOMPRAPPTO.COMPANIA = D_ORDENDECOMPRA.COMPANIA 
                AND ORDENDECOMPRAPPTO.CLASEORDEN = D_ORDENDECOMPRA.CLASEORDEN 
                AND ORDENDECOMPRAPPTO.NUMERO = D_ORDENDECOMPRA.ORDENDECOMPRA 
                AND ORDENDECOMPRAPPTO.TIPOPPTO = D_ORDENDECOMPRA.TIPOPPTO 
                AND ORDENDECOMPRAPPTO.NUMEROPPTO = D_ORDENDECOMPRA.NUMEROPPTO   
    WHERE NVL(ORDENDECOMPRA.ESTADO,'V')<>'A' 
        AND DETALLE_COMPROBANTE_PPTAL.COMPANIA= s$compania$s
        AND (DETALLE_COMPROBANTE_PPTAL.FECHA 
            BETWEEN s$fechaInicial$s
            AND s$fechaFinal$s)   
        AND DETALLE_COMPROBANTE_PPTAL.CUENTA 
            BETWEEN 's$rubroInicial$s'
            AND 's$rubroFinal$s' 
    GROUP BY DETALLE_COMPROBANTE_PPTAL.COMPANIA, 
        ORDENDECOMPRA.CLASEORDEN, 
        ORDENDECOMPRAPPTO.NUMERO, 
        DETALLE_COMPROBANTE_PPTAL.CUENTA, 
        V_PLAN_PRESUPUESTAL.NOMBRE, 
        TERCERO.NOMBRE,         
        TIPOORDENDECOMPRA.NOMBRE, 
        ORDENDECOMPRA.CLASEORDEN ||' - '|| ORDENDECOMPRA.NUMERO, 
        ORDENDECOMPRA.FECHA, 
        ORDENDECOMPRAPPTO.NUMEROPPTO, 
        ORDENDECOMPRA.NUMERO 
    ORDER BY DETALLE_COMPROBANTE_PPTAL.CUENTA),
D_ODC AS (SELECT D_ORDENDECOMPRA.COMPANIA, 
        D_ORDENDECOMPRA.CLASEORDEN, 
        D_ORDENDECOMPRA.ORDENDECOMPRA  NUMERO,
        D_ORDENDECOMPRA.NUMEROPPTO, 
        COUNT(D_ORDENDECOMPRA.COMPANIA) CANTIDAD,
        D_ORDENDECOMPRA.VLRTOTAL VALOR
    FROM D_ORDENDECOMPRA INNER JOIN TMP_DISPONIBILIDADES
        ON D_ORDENDECOMPRA.COMPANIA =TMP_DISPONIBILIDADES.COMPANIA
                AND D_ORDENDECOMPRA.CLASEORDEN = TMP_DISPONIBILIDADES.CLASEORDEN 
                AND D_ORDENDECOMPRA.ORDENDECOMPRA = TMP_DISPONIBILIDADES.NUMEROODC
    WHERE D_ORDENDECOMPRA.NUMEROPPTO IS NOT NULL
    GROUP BY D_ORDENDECOMPRA.COMPANIA, D_ORDENDECOMPRA.CLASEORDEN, D_ORDENDECOMPRA.ORDENDECOMPRA, D_ORDENDECOMPRA.NUMEROPPTO, D_ORDENDECOMPRA.VLRTOTAL)
SELECT TMP_DISPONIBILIDADES.COMPANIA, 
    TMP_DISPONIBILIDADES.CLASEORDEN, 
    TMP_DISPONIBILIDADES.NUMEROODCPPTO ORDENDECOMPRAPPTO_NUMERO, 
    TMP_DISPONIBILIDADES.CUENTA, 
    TMP_DISPONIBILIDADES.NCUENTA, 
    TMP_DISPONIBILIDADES.CLIENTE, 
    TMP_DISPONIBILIDADES.TIPO, 
    TMP_DISPONIBILIDADES.TIPOYNOMBRE, 
    TMP_DISPONIBILIDADES.TIPOYNUMERO, 
    TO_CHAR(TMP_DISPONIBILIDADES.FECHA,'DD/MM/YYYY') FECHA, 
    (CASE WHEN TMP_DISPONIBILIDADES.VLR_ORDPPTO IS NULL 
        THEN D_ODC.VALOR
        ELSE TMP_DISPONIBILIDADES.VLR_ORDPPTO
    END) VLR_ORDPPTO, 
    TMP_DISPONIBILIDADES.VLR_ORDENC, 
    TMP_DISPONIBILIDADES.NUMEROPPTO, 
    TMP_DISPONIBILIDADES.NUMEROODC ORDENDECOMPRA_NUMERO
FROM TMP_DISPONIBILIDADES INNER JOIN D_ODC 
    ON TMP_DISPONIBILIDADES.COMPANIA = D_ODC.COMPANIA
        AND TMP_DISPONIBILIDADES.CLASEORDEN = D_ODC.CLASEORDEN 
        AND TMP_DISPONIBILIDADES.NUMEROODC = D_ODC.NUMERO 
        AND TMP_DISPONIBILIDADES.CANTIDADRS = CANTIDAD