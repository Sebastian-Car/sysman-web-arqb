SELECT DISTINCT
      ORDENDECOMPRA.COMPANIA,
      TERCERO.NOMBRE, 
      ORDENDECOMPRA.CLASEORDEN, 
      ORDENDECOMPRA.NUMERO NUMEROCONTRATO, 
      '' DISPONIBILIDAD, 
      ORDENDECOMPRA.OBJETOCONTRATO, 
      ' ' RUBRO, 
      ORDENDECOMPRA.PLAZODEENTREGA, 
      ORDENDECOMPRA.TERCERO, 
      ORDENDECOMPRA.FECHA, 
      TIPOORDENDECOMPRA.NOMBRE NOMBRETIPO, 
      PCK_CONTRATOS.FC_NOPOLIZA(ORDENDECOMPRA.CLASEORDEN,ORDENDECOMPRA.NUMERO,ORDENDECOMPRA.COMPANIA) NoPoliza, 
      NVL(SUMADEVALORTOTAL,0) ADICIONES, 
      CASE ORDENDECOMPRA.ESTADO 
        WHEN 'V' THEN 'Vigente' 
        WHEN 'A' THEN 'Anulado'
        WHEN 'L' THEN 'Liquido'
        WHEN 'T' THEN 'Terminado'
        WHEN 'S' THEN 'Suspendido'
        WHEN 'P' THEN 'En Proceso de Sanción o Proceso Judicial'
      ELSE 
        'TODOS'
      END ESTADO, 
      ORDENDECOMPRA.FECHAFINALIZACION, 
      ORDENDECOMPRA.INTERVENTOR, 
      ORDENDECOMPRA.CEDULAINTERVENTOR, 
      ORDENDECOMPRA.VALORTOTAL, 
      ORDENDECOMPRA.VALORPAGOS,
      ORDENDECOMPRA.VALORFINAL,
      ROUND(ORDENDECOMPRA.VALORPAGOS/(CASE WHEN ORDENDECOMPRA.VALORTOTAL=0 THEN 1 ELSE ORDENDECOMPRA.VALORTOTAL END)*100,2)|| '%'  PORC_EJECUTADO,
      (ORDENDECOMPRA.VALORTOTAL-VALORPAGOS)  VALOR_SINEJEC,
      ROUND(100-(VALORPAGOS/(CASE WHEN ORDENDECOMPRA.VALORTOTAL=0 THEN 1 ELSE ORDENDECOMPRA.VALORTOTAL END )*100),2)|| '%'  PORC_SINEJECUTAR,
      TERCEROSUP.NOMBRE SUPERVISOR,
      ORDENDECOMPRA.FECHALIQUIDACION,
      ORDENDECOMPRA.FECHAFIRMA,
      (ORDENDECOMPRA.FECHAFINALIZACION- ORDENDECOMPRA.FECHAFINAL) PRORROGAS_TIEMPO,
      ORDENDECOMPRA.VALOR_ANTICIPO ANTICIPO,
      ORDENDECOMPRA.VALORTOTAL+NVL(SUMADEVALORTOTAL,0) VALOR,
      ORDENDECOMPRA.FECHAFINALACTUAL,
      SUMATORIAPAGOS.TOTAL PAGOSN,
      (ORDENDECOMPRA.VALORTOTAL-SUMATORIAPAGOS.TOTAL) SALDOS,
      TERCERO.NIT
FROM ORDENDECOMPRA 
  INNER JOIN TERCERO 
    ON  ORDENDECOMPRA.COMPANIA    = TERCERO.COMPANIA 
    AND ORDENDECOMPRA.TERCERO     = TERCERO.NIT
    AND ORDENDECOMPRA.SUCURSAL    = TERCERO.SUCURSAL          
  INNER JOIN TIPOORDENDECOMPRA 
    ON  ORDENDECOMPRA.COMPANIA    = TIPOORDENDECOMPRA.COMPANIA 
    AND ORDENDECOMPRA.CLASEORDEN  = TIPOORDENDECOMPRA.CODIGO
  LEFT JOIN ( SELECT  ORDENDECOMPRA.COMPANIA, 
                      ORDENDECOMPRA.TIPOAFECTADO,
                      ORDENDECOMPRA.NUMEROAFECTADO,
                      SUM(ORDENDECOMPRA.VALORTOTAL) SUMADEVALORTOTAL
              FROM ORDENDECOMPRA
              GROUP BY  ORDENDECOMPRA.COMPANIA, 
                        ORDENDECOMPRA.TIPOAFECTADO, 
                        ORDENDECOMPRA.NUMEROAFECTADO)ADICIONESCONTRATOS 
    ON  ORDENDECOMPRA.COMPANIA    = ADICIONESCONTRATOS.COMPANIA 
    AND ORDENDECOMPRA.CLASEORDEN  = ADICIONESCONTRATOS.TIPOAFECTADO 
    AND ORDENDECOMPRA.NUMERO      = ADICIONESCONTRATOS.NUMEROAFECTADO 
  LEFT JOIN SUPERVISORES 
    ON ORDENDECOMPRA.COMPANIA     = SUPERVISORES.COMPANIA 
    AND ORDENDECOMPRA.CLASEORDEN  = SUPERVISORES.CLASEORDEN 
    AND ORDENDECOMPRA.NUMERO      = SUPERVISORES.NUMEROCONTRATO
  LEFT JOIN ORDENDECOMPRAPPTO
    ON  ORDENDECOMPRA.COMPANIA    = ORDENDECOMPRAPPTO.COMPANIA
    AND ORDENDECOMPRA.CLASEORDEN  = ORDENDECOMPRAPPTO.CLASEORDEN
    AND ORDENDECOMPRA.NUMERO      = ORDENDECOMPRAPPTO.NUMERO
  INNER JOIN TIPO_COMPROBPP
    ON ORDENDECOMPRAPPTO.COMPANIA = TIPO_COMPROBPP.COMPANIA
    AND ORDENDECOMPRAPPTO.TIPOPPTO =  TIPO_COMPROBPP.CODIGO 
   LEFT JOIN TERCERO TERCEROSUP
    ON  SUPERVISORES.COMPANIA    = TERCEROSUP.COMPANIA 
    AND SUPERVISORES.CEDULA      = TERCEROSUP.NIT
    AND SUPERVISORES.SUCURSAL    = TERCEROSUP.SUCURSAL  
   LEFT JOIN (SELECT NOVEDADCONTRATO.COMPANIA,
                    NOVEDADCONTRATO.CLASEORDEN,
                    NOVEDADCONTRATO.ORDENDECOMPRA,
                    SUM(VALORTOTAL) AS TOTAL
                  FROM NOVEDADCONTRATO
                  WHERE NOVEDADCONTRATO.COMPANIA = '001'
                  AND INSTR(PCK_SYSMAN_UTL.FC_COLOCARCOMILLAS(REPLACE(''
                    ||PCK_SYSMAN_UTL.FC_PAR(COMPANIA, 'TIPO DE ACTA ANTICIPO', 9, SYSDATE, -1)
                    ||'',' ','')),''''
                    ||NOVEDADCONTRATO.TIPOT
                    ||'''',1)                = 0
                  AND NOVEDADCONTRATO.CLASET ='N'
                  GROUP BY NOVEDADCONTRATO.COMPANIA,
                    NOVEDADCONTRATO.CLASEORDEN,
                    NOVEDADCONTRATO.ORDENDECOMPRA) SUMATORIAPAGOS
    ON SUMATORIAPAGOS.COMPANIA = ORDENDECOMPRA.COMPANIA
    AND SUMATORIAPAGOS.CLASEORDEN = ORDENDECOMPRA.CLASEORDEN
    AND SUMATORIAPAGOS.ORDENDECOMPRA = ORDENDECOMPRA.NUMERO
WHERE ORDENDECOMPRA.COMPANIA= s$compania$s 
  AND ORDENDECOMPRA.ESTADO = CASE WHEN 's$estado$s' = 'D' THEN ORDENDECOMPRA.ESTADO ELSE 's$estado$s' END 
  AND TIPOORDENDECOMPRA.CODIGO BETWEEN 's$tipoContratoInicial$s' AND 's$tipoContratoFinal$s'  
  AND TERCERO.NIT  BETWEEN 's$terceroIni$s' AND  's$terceroFin$s'  
  AND  ORDENDECOMPRA.FECHA  BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY HH24:mi:ss') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY HH24:mi:ss')  
  AND NVL(TIPOORDENDECOMPRA.CLASE,'')   NOT IN('M')
  AND (TIPO_COMPROBPP.CLASE IN ('DIS') OR ORDENDECOMPRAPPTO.TIPOPPTO IS NULL)
ORDER BY TERCERO.NIT, TERCERO.NOMBRE, TIPOORDENDECOMPRA.NOMBRE, ORDENDECOMPRA.COMPANIA, ORDENDECOMPRA.CLASEORDEN
