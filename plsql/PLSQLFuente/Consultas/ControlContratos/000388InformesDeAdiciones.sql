SELECT DISTINCT 
      NVL(AFECTADA_TIPOORDENDECOMPRA.NOMBRE,' ') NOMBREAFECTADA,
      ORDENDECOMPRA.AJUSTEPESO,
      D_ORDENDECOMPRA.CANTIDAD,
      ORDENDECOMPRA.CLASEORDEN,
      D_ORDENDECOMPRA.CODIGO,
      ORDENDECOMPRA.COMPANIA, 
      NOMBRELARGO || SUBSTR(D_ORDENDECOMPRA.ESPECIFICACION,1,255) || D_ORDENDECOMPRA.MARCA "DESC",
      D_ORDENDECOMPRA.ELEMENTO,
      TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') FECHA,
      ORDENDECOMPRA.NUMERO,
      ORDENDECOMPRA.NUMEROAFECTADO,
      SUBSTR(ORDENDECOMPRA.OBJETOCONTRATO,1,255) OBJETOCONTRATOF,
      D_ORDENDECOMPRA.ORDENDECOMPRA,
      TERCERO.NOMBRE NOMBRETERCERO,
      ORDENDECOMPRA.TERCERO, 
      INVENTARIO.UNIDAD, 
      D_ORDENDECOMPRA.VALORUNITARIO, 
      D_ORDENDECOMPRA.VLRDESCUENTO, 
      D_ORDENDECOMPRA.VLRIVA,
      TIPOORDENDECOMPRA.NOMBRE NOMBRETIPO,
      D_ORDENDECOMPRA.VLRTOTAL
FROM ORDENDECOMPRA INNER JOIN TERCERO 
  ON  ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA
  AND ORDENDECOMPRA.TERCERO = TERCERO.NIT 
  AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL 
INNER JOIN TIPOORDENDECOMPRA 
  ON ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA 
  AND ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO  
LEFT JOIN TIPOORDENDECOMPRA  AFECTADA_TIPOORDENDECOMPRA 
  ON ORDENDECOMPRA.COMPANIA = AFECTADA_TIPOORDENDECOMPRA.COMPANIA 
  AND ORDENDECOMPRA.TIPOAFECTADO = AFECTADA_TIPOORDENDECOMPRA.CODIGO 
LEFT JOIN D_ORDENDECOMPRA 
  ON ORDENDECOMPRA.COMPANIA = D_ORDENDECOMPRA.COMPANIA 
  AND ORDENDECOMPRA.CLASEORDEN = D_ORDENDECOMPRA.CLASEORDEN 
  AND ORDENDECOMPRA.NUMERO = D_ORDENDECOMPRA.ORDENDECOMPRA 
LEFT JOIN INVENTARIO 
  ON D_ORDENDECOMPRA.COMPANIA = INVENTARIO.COMPANIA 
  AND D_ORDENDECOMPRA.ELEMENTO = INVENTARIO.CODIGOELEMENTO
WHERE TIPOORDENDECOMPRA.COMPANIA = s$compania$s 
  AND ORDENDECOMPRA.CLASEORDEN = 's$tipomodificacion$s'
  AND ORDENDECOMPRA.NUMERO = s$numero$s
GROUP BY AFECTADA_TIPOORDENDECOMPRA.NOMBRE,
      ORDENDECOMPRA.AJUSTEPESO,
      D_ORDENDECOMPRA.CANTIDAD,
      ORDENDECOMPRA.CLASEORDEN,
      D_ORDENDECOMPRA.CODIGO,
      ORDENDECOMPRA.COMPANIA, 
      NOMBRELARGO || SUBSTR(D_ORDENDECOMPRA.ESPECIFICACION,1,255) || D_ORDENDECOMPRA.MARCA,
      D_ORDENDECOMPRA.ELEMENTO,
      TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY'),
      ORDENDECOMPRA.NUMERO,
      ORDENDECOMPRA.NUMEROAFECTADO,
      SUBSTR(ORDENDECOMPRA.OBJETOCONTRATO,1,255),
      D_ORDENDECOMPRA.ORDENDECOMPRA,
      TERCERO.NOMBRE,
      ORDENDECOMPRA.TERCERO, 
      INVENTARIO.UNIDAD, 
      D_ORDENDECOMPRA.VALORUNITARIO, 
      D_ORDENDECOMPRA.VLRDESCUENTO, 
      D_ORDENDECOMPRA.VLRIVA,
      TIPOORDENDECOMPRA.NOMBRE,
      D_ORDENDECOMPRA.VLRTOTAL
