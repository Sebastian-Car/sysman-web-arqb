SELECT ORDENDECOMPRA.COMPANIA, 
    UPPER(CLASEORDEN) CLASEORDEN, 
    ORDENDECOMPRA.NUMERO, 
    ORDENDECOMPRA.OBJETOCONTRATO, 
    ORDENDECOMPRA.VALORTOTAL, 
    ORDENADOR.NOMBRE ORDENADOR, 
    TERCERO_1.NOMBRE INTERVENTOR, 
    TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') FECHA, 
    TO_CHAR(ORDENDECOMPRA.FECHAFINALIZACION ,'DD/MM/YYYY') FECHAFINALIZACION, 
    ORDENDECOMPRA.TERCERO, 
    TERCERO.NOMBRE NOMBRECONTRATISTA,
    DECODE (ORDENDECOMPRA.ESTADO, 'V','Vigente','A','Anulada','T','Terminada','L','Liquidada',
            'S','Suspendido','M','Terminado por mutuo acuerdo','P','En proceso de sanción o proceso judicial') ESTADO
FROM ORDENDECOMPRA LEFT JOIN TERCERO 
    ON ORDENDECOMPRA.TERCERO = TERCERO.NIT 
        AND ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA 
    LEFT JOIN ORDENADOR 
        ON ORDENDECOMPRA.ORDENADOR = ORDENADOR.CEDULA 
            AND ORDENDECOMPRA.COMPANIA = ORDENADOR.COMPANIA 
    LEFT JOIN TERCERO  TERCERO_1 
        ON ORDENDECOMPRA.CEDULAINTERVENTOR = TERCERO_1.NIT 
            AND ORDENDECOMPRA.COMPANIA = TERCERO_1.COMPANIA
WHERE ORDENDECOMPRA.COMPANIA = s$compania$s 
    AND UPPER(CLASEORDEN) 
        BETWEEN 's$tipoContratoInicial$s' 
            AND 's$tipoContratoFinal$s' 
    AND ORDENDECOMPRA.FECHA 
        BETWEEN s$fechaInicial$s 
            AND s$fechaFinal$s
    AND TERCERO.NIT
        BETWEEN 's$terceroCed$s' 
            AND 's$terceroCedFinal$s'
ORDER BY TERCERO.NOMBRE, ORDENDECOMPRA.NUMERO