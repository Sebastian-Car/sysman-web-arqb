SELECT NOVEDADCONTRATO.NOVEDAD, 
       TO_CHAR(NOVEDADCONTRATO.FECHA,'DD/MM/YYYY') FECHA,
       NOVEDADCONTRATO.VALORTOTAL,
       NOVEDADCONTRATO.COMPANIA, 
       NOVEDADCONTRATO.CLASEORDEN, 
       NOVEDADCONTRATO.ORDENDECOMPRA
FROM NOVEDADCONTRATO
WHERE  
       NOVEDADCONTRATO.CLASET        = 'P'
       AND COMPANIA = $P{PR_COMPANIA}
       AND ORDENDECOMPRA = $P{PR_ORDENDECOMPRA}
       AND CLASEORDEN = $P{PR_CLASEORDEN}