SELECT SP_USUARIO.COMPANIA,
  SP_USUARIO.PRIMERAPELLIDO||' '||SP_USUARIO.SEGUNDOAPELLIDO||' '||SP_USUARIO.NOMBRES NOMBRE,
  SP_USUARIO.CICLO,
  s$sumPesoAseo$s AS PESOASEO,
  SP_DETALLEDISPOSICION.ANO,
  SP_DETALLEDISPOSICION.PERIODO,
  SP_USUARIO.CODIGORUTA
FROM SP_USUARIO
INNER JOIN SP_DETALLEDISPOSICION
ON (SP_USUARIO.CODIGORUTA           = SP_DETALLEDISPOSICION.CODIGORUTA)
AND (SP_USUARIO.CICLO               = SP_DETALLEDISPOSICION.CICLO)
AND (SP_USUARIO.COMPANIA            = SP_DETALLEDISPOSICION.COMPANIA)
WHERE (((SP_USUARIO.COMPANIA)       =s$compania$s)
AND ((SP_DETALLEDISPOSICION.ANO)    =s$ano$s)
AND ((SP_DETALLEDISPOSICION.PERIODO)='s$periodo$s')
AND ((SP_USUARIO.CODIGORUTA) BETWEEN 's$codigoRutaInicial$s' AND 's$codigoRutaFinal$s'))
s$groupBy$s
