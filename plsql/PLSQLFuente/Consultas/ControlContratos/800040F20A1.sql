WITH RUBRO_VIGFUTURA AS (
SELECT DETALLE_COMPROBANTE_PPTAL.COMPANIA,
   COMPROBANTE_PPTAL.TIPOCONTRATO, 
   COMPROBANTE_PPTAL.NUMEROCONTRATO,
   COMPROBANTE_PPTAL.FECHA_APROB_VF, 
   COMPROBANTE_PPTAL.FECHA_AUTO_VF
FROM (DETALLE_COMPROBANTE_PPTAL INNER JOIN 
        COMPROBANTE_PPTAL ON
        DETALLE_COMPROBANTE_PPTAL.COMPANIA = COMPROBANTE_PPTAL.COMPANIA AND 
        DETALLE_COMPROBANTE_PPTAL.ANO = COMPROBANTE_PPTAL.ANO AND 
        DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = COMPROBANTE_PPTAL.TIPO AND
        DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO) 
        INNER JOIN V_PLAN_PRESUPUESTAL ON 
        DETALLE_COMPROBANTE_PPTAL.CUENTA = V_PLAN_PRESUPUESTAL.ID AND 
        DETALLE_COMPROBANTE_PPTAL.ANO = V_PLAN_PRESUPUESTAL.ANO AND 
        DETALLE_COMPROBANTE_PPTAL.COMPANIA = V_PLAN_PRESUPUESTAL.COMPANIA
  WHERE DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE IN ('RES','ADI','REX')
    AND DETALLE_COMPROBANTE_PPTAL.ANO=EXTRACT (YEAR FROM SYSDATE)
    AND COMPROBANTE_PPTAL.TIPOCONTRATO IS NOT NULL 
    AND V_PLAN_PRESUPUESTAL.TIPOVIGENCIA='VF'
GROUP BY DETALLE_COMPROBANTE_PPTAL.COMPANIA, 
        DETALLE_COMPROBANTE_PPTAL.ANO, 
        COMPROBANTE_PPTAL.TIPOCONTRATO, 
        COMPROBANTE_PPTAL.NUMEROCONTRATO,
        DETALLE_COMPROBANTE_PPTAL.CUENTA, 
        DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
        DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE, 
        V_PLAN_PRESUPUESTAL.TIPOVIGENCIA,
        COMPROBANTE_PPTAL.FECHA_APROB_VF,
        COMPROBANTE_PPTAL.TIPO_VF, 
        COMPROBANTE_PPTAL.TIPO_AUTORIZACION_VF,
        COMPROBANTE_PPTAL.NRO_AUTORIZACION_VF, 
        COMPROBANTE_PPTAL.FECHA_AUTO_VF 
ORDER BY DETALLE_COMPROBANTE_PPTAL.CUENTA
),
 
RUBROCONTRATO_FUENTESR AS (
SELECT DETALLE_COMPROBANTE_PPTAL.COMPANIA, 
DETALLE_COMPROBANTE_PPTAL.ANO,
COMPROBANTE_PPTAL.TIPOCONTRATO, 
COMPROBANTE_PPTAL.NUMEROCONTRATO, 
SUM( CASE FUENTE_RECURSOS.CODSIA_F20 WHEN 'REGALIAS' THEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO ELSE 0 END) AS VALORREGALIAS, 
SUM(CASE FUENTE_RECURSOS.CODSIA_F20 WHEN 'RECURSOS PROPIOS' THEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO ELSE 0 END) AS VALORPROPIOS,
DETALLE_COMPROBANTE_PPTAL.CUENTA, 
DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE, 
PLAN_PRESUPUESTAL.FUENTE_RECURSO, 
NVL(SUM(CASE FUENTE_RECURSOS.CODSIA_F20 WHEN 'COLOMBIA HUMANITARIA' THEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO ELSE URGENCIAMANIFIESTA.VALORCOLHUMANITARIA END),0) AS COLHUMANITARIA, 
SUM(CASE FUENTE_RECURSOS.CODSIA_F20 WHEN 'RECURSOS SGP' THEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO ELSE 0 END) AS VALORSGP,
URGENCIAMANIFIESTA.NUMEROACTO, 
URGENCIAMANIFIESTA.FECHAACTO, 
URGENCIAMANIFIESTA.VALORURGENCIA, 
URGENCIAMANIFIESTA.HUMANITARIA, 
URGENCIAMANIFIESTA.REHABILITACION,
URGENCIAMANIFIESTA.RECONSTRUCCION
FROM (((DETALLE_COMPROBANTE_PPTAL 
    INNER JOIN COMPROBANTE_PPTAL ON 
    DETALLE_COMPROBANTE_PPTAL.COMPANIA = COMPROBANTE_PPTAL.COMPANIA AND
    DETALLE_COMPROBANTE_PPTAL.ANO = COMPROBANTE_PPTAL.ANO AND 
    DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = COMPROBANTE_PPTAL.TIPO AND 
    DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO)
    LEFT JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL ON 
    DETALLE_COMPROBANTE_PPTAL.CUENTA = PLAN_PRESUPUESTAL.ID AND
    DETALLE_COMPROBANTE_PPTAL.ANO = PLAN_PRESUPUESTAL.ANO AND
    DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA)
    LEFT JOIN FUENTE_RECURSOS ON 
    PLAN_PRESUPUESTAL.FUENTE_RECURSO = FUENTE_RECURSOS.CODIGO AND 
    PLAN_PRESUPUESTAL.ANO = FUENTE_RECURSOS.ANO AND
    PLAN_PRESUPUESTAL.COMPANIA = FUENTE_RECURSOS.COMPANIA)
    INNER JOIN URGENCIAMANIFIESTA ON 
    COMPROBANTE_PPTAL.NUMEROCONTRATO = URGENCIAMANIFIESTA.ORDENDECOMPRA AND 
    COMPROBANTE_PPTAL.TIPOCONTRATO = URGENCIAMANIFIESTA.CLASEORDEN AND 
    COMPROBANTE_PPTAL.COMPANIA = URGENCIAMANIFIESTA.COMPANIA
WHERE DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE IN ('RES','ADI','REX')
  AND DETALLE_COMPROBANTE_PPTAL.ANO = EXTRACT (YEAR FROM SYSDATE)
  AND COMPROBANTE_PPTAL.TIPOCONTRATO IS NOT NULL
GROUP BY DETALLE_COMPROBANTE_PPTAL.COMPANIA,
    DETALLE_COMPROBANTE_PPTAL.ANO, 
    COMPROBANTE_PPTAL.TIPOCONTRATO, 
    COMPROBANTE_PPTAL.NUMEROCONTRATO, 
    DETALLE_COMPROBANTE_PPTAL.CUENTA,
    DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
    DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
    PLAN_PRESUPUESTAL.FUENTE_RECURSO,
    URGENCIAMANIFIESTA.NUMEROACTO,
    URGENCIAMANIFIESTA.FECHAACTO,
    URGENCIAMANIFIESTA.VALORURGENCIA,
    URGENCIAMANIFIESTA.HUMANITARIA,
    URGENCIAMANIFIESTA.REHABILITACION,
    URGENCIAMANIFIESTA.RECONSTRUCCION
ORDER BY DETALLE_COMPROBANTE_PPTAL.CUENTA),

ADICION_CONTRATOS AS (
SELECT ORDENDECOMPRA.COMPANIA, 
ORDENDECOMPRA.CLASEORDEN,
ORDENDECOMPRA.NUMERO, 
ORDENDECOMPRA.TERCERO, 
ORDENDECOMPRA.FECHA AS FECHAADICION, 
SUM(ORDENDECOMPRA.VALORTOTAL) AS VALORADICION, 
EXTRACT (YEAR FROM FECHA) AS ANO,
TIPOORDENDECOMPRA.CLASE AS EXPR1,
ORDENDECOMPRA.TIPOAFECTADO,
ORDENDECOMPRA.NUMEROAFECTADO ,
ORDENDECOMPRA.PLAZODEENTREGA
FROM ORDENDECOMPRA 
INNER JOIN TIPOORDENDECOMPRA ON 
ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO AND
ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA
WHERE 
EXTRACT (YEAR FROM FECHA) = EXTRACT (YEAR FROM SYSDATE)
AND TIPOORDENDECOMPRA.CLASE='M'
GROUP BY ORDENDECOMPRA.COMPANIA, 
ORDENDECOMPRA.CLASEORDEN,
ORDENDECOMPRA.NUMERO, 
ORDENDECOMPRA.TERCERO, 
ORDENDECOMPRA.FECHA, 
EXTRACT (YEAR FROM FECHA), 
TIPOORDENDECOMPRA.CLASE,
ORDENDECOMPRA.TIPOAFECTADO, 
ORDENDECOMPRA.NUMEROAFECTADO,
ORDENDECOMPRA.PLAZODEENTREGA
)

SELECT ORDENDECOMPRA.COMPANIA,
        ORDENDECOMPRA.CLASEORDEN, 
        PCK_SYSMAN_UTL.FC_PAR(ORDENDECOMPRA.COMPANIA,'REGIMEN DE CONTRATACIÓN',9,SYSDATE) AS REGIMEN, 
        'MUNICIPAL' AS ORIGENPPTO,
        ORDENDECOMPRA.NUMERO AS NUMEROCONTRATO, 
        TIPOADJUDICACION.EQUIVALENTESIA AS MODALIDADSELECCION, 
        TIPOADJUDICACION.PROCEDIMIENTOSIA AS PROCEDIMIENTO, 
        TIPOORDENDECOMPRA.TIPOCONTRATO_SIA,
        SECTORES.SECTOR_SIA AS SECTOR, 
        MIN(ORDENDECOMPRA.OBJETOCONTRATO)  KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) AS OBJETOCONTRATO,
        ORDENDECOMPRA.VALORTOTAL AS VALORINICIAL, 
        ORDENDECOMPRA.NOMBRECONTRATISTA,
        ORDENDECOMPRA.TERCERO AS NITTERCERO, 
        CASE TERCERO.NATURALEZA WHEN 'J' THEN 'JURIDICA' ELSE 'NATURAL' END AS TIPOPERSONA, 
        RUBROCONTRATO_FUENTESR.CUENTA,
        CASE ORDENDECOMPRA.FECHAFIRMA WHEN  NULL THEN ORDENDECOMPRA.FECHA ELSE ORDENDECOMPRA.FECHAFIRMA END AS FECHASUSCRIP, 
        ORDENDECOMPRA.INTERVENTOR, 
        ORDENDECOMPRA.CEDULAINTERVENTOR, 
        CASE ORDENDECOMPRA.TIPOINTERVENTOR WHEN 'I' THEN 'INTERNO' ELSE 'EXTERNO' END AS VINCULAINTERVENTOR, 
        'DIAS' AS NUMEROUND, 
        ORDENDECOMPRA.PLAZODEENTREGA,
        CASE WHEN NVL(ORDENDECOMPRA.VALOR_ANTICIPO,0)=0 THEN 'NO' ELSE 'SI' END AS ANTICIPOPACTADO, 
        ORDENDECOMPRA.VALOR_ANTICIPO AS ANTICIPOS,                                                  
        CASE ORDENDECOMPRA.FECHAINICIO WHEN  NULL THEN ORDENDECOMPRA.FECHA ELSE ORDENDECOMPRA.FECHAINICIO END AS ACTAINICIO, 
        ORDENDECOMPRA.FECHAFINALIZACION, 
        CASE ADICION_CONTRATOS.VALORADICION WHEN 0 THEN 'SI' ELSE 'NO' END AS PRORROGAS,
        NVL(VALORADICION,0) AS VALORADI,
        ORDENDECOMPRA.FECHALIQUIDACION FECHALIQUIDACION, 
        CASE WHEN ORDENDECOMPRA.EMERGENCIAIN=0 OR ORDENDECOMPRA.EMERGENCIAIN IS NULL THEN 'NO' ELSE 'SI' END AS URGMANIFIESTA,
        RUBROCONTRATO_FUENTESR.NUMEROACTO, 
        RUBROCONTRATO_FUENTESR.FECHAACTO FECHAACTO,
        RUBROCONTRATO_FUENTESR.VALORURGENCIA, 
        NVL(VALORPROPIOS,0) AS PROPIOS,
        NVL(VALORREGALIAS,0) AS REGALIAS, 
        NVL(VALORSGP,0) AS SGP, 
        NVL(COLHUMANITARIA,0) AS VLRCOLHUMANITARIA,
        TIPOORDENDECOMPRA.CLASE,
        CASE RUBRO_VIGFUTURA.NUMEROCONTRATO WHEN NULL THEN 'NO' ELSE 'SI' END AS VF, 
        RUBRO_VIGFUTURA.FECHA_AUTO_VF, 
        TO_CHAR(RUBRO_VIGFUTURA.FECHA_AUTO_VF,'YYYY/MM/DD') AS VFANOINICIAL, 
        EXTRACT (YEAR FROM RUBRO_VIGFUTURA.FECHA_APROB_VF) AS VFANOFINAL,
        ORDENDECOMPRA.FECHAPUBLICACION,
        SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE) AS PRESUPUESTO,
        CASE WHEN V_RESUMENPPTO_BASE.TIPOVIGENCIA IN('VF') THEN SUM(V_RESUMENPPTO_BASE.APROPIADO) END AS VFAPRINICIAL,
        CASE WHEN V_RESUMENPPTO_BASE.TIPOVIGENCIA IN('VF') THEN SUM(V_RESUMENPPTO_BASE.APROPIADO) + V_RESUMENPPTO_BASE.ADICIONES - V_RESUMENPPTO_BASE.REDUCCIONES END AS VFAUTORIZADO,
        CASE WHEN V_RESUMENPPTO_BASE.TIPOVIGENCIA IN('VF') THEN (SUM(V_RESUMENPPTO_BASE.APROPIADO) + V_RESUMENPPTO_BASE.ADICIONES - V_RESUMENPPTO_BASE.REDUCCIONES) - SUM(V_RESUMENPPTO_BASE.COMPROMISOSACUM) END AS VFSALDO,
        CASE WHEN V_RESUMENPPTO_BASE.TIPOVIGENCIA IN('VF') THEN SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT) END AS VFPERIODO,
        TIPODESTINO.NOMBRE AS TIPOGASTO,
        CLASETIPOORDENDECOMPRA.SIAF20                                                                         
FROM ((((((ORDENDECOMPRA 
    LEFT JOIN ORDENDECOMPRAPPTO ON
    ORDENDECOMPRA.COMPANIA = ORDENDECOMPRAPPTO.COMPANIA AND 
    ORDENDECOMPRA.NUMERO = ORDENDECOMPRAPPTO.NUMERO AND 
    ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRAPPTO.CLASEORDEN 
    LEFT JOIN V_RESUMENPPTO_BASE ON
    ORDENDECOMPRAPPTO.COMPANIA = V_RESUMENPPTO_BASE.COMPANIA AND 
    ORDENDECOMPRAPPTO.ANOPPTO = V_RESUMENPPTO_BASE.ANO AND 
    ORDENDECOMPRAPPTO.RUBRO = V_RESUMENPPTO_BASE.CODIGO AND
    V_RESUMENPPTO_BASE.ANO = EXTRACT (YEAR FROM ORDENDECOMPRA.FECHA) AND
    V_RESUMENPPTO_BASE.MES = EXTRACT (MONTH FROM ORDENDECOMPRA.FECHA)
    LEFT JOIN TIPOADJUDICACION ON 
    ORDENDECOMPRA.TIPOADJUDICACION = TIPOADJUDICACION.CODIGO) 
    INNER JOIN TIPOORDENDECOMPRA ON 
    ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA AND
    ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO) 
    LEFT JOIN ADICION_CONTRATOS 
    ON (ORDENDECOMPRA.COMPANIA = ADICION_CONTRATOS.COMPANIA) AND
    ORDENDECOMPRA.CLASEORDEN = ADICION_CONTRATOS.TIPOAFECTADO AND 
    ORDENDECOMPRA.NUMERO = ADICION_CONTRATOS.NUMEROAFECTADO) 
    LEFT JOIN RUBROCONTRATO_FUENTESR ON                               
    ORDENDECOMPRA.COMPANIA = RUBROCONTRATO_FUENTESR.COMPANIA AND 
    ORDENDECOMPRA.CLASEORDEN = RUBROCONTRATO_FUENTESR.TIPOCONTRATO AND 
    ORDENDECOMPRA.NUMERO = RUBROCONTRATO_FUENTESR.NUMEROCONTRATO) 
    LEFT JOIN SECTORES ON 
    ORDENDECOMPRA.SECTOR = SECTORES.CODIGO) 
    INNER JOIN TERCERO ON
    ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL AND 
    ORDENDECOMPRA.TERCERO = TERCERO.NIT AND 
    ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA)
    LEFT JOIN RUBRO_VIGFUTURA ON
    ORDENDECOMPRA.NUMERO = RUBRO_VIGFUTURA.NUMEROCONTRATO AND 
    ORDENDECOMPRA.CLASEORDEN = RUBRO_VIGFUTURA.TIPOCONTRATO AND 
    ORDENDECOMPRA.COMPANIA = RUBRO_VIGFUTURA.COMPANIA 
    LEFT JOIN V_PLAN_PRESUPUESTAL ON
    ORDENDECOMPRAPPTO.COMPANIA = V_PLAN_PRESUPUESTAL.COMPANIA AND 
    ORDENDECOMPRAPPTO.ANOPPTO = V_PLAN_PRESUPUESTAL.ANO AND 
    ORDENDECOMPRAPPTO.RUBRO = V_PLAN_PRESUPUESTAL.CODIGO 
    LEFT JOIN TIPODESTINO ON
    TIPODESTINO.CODIGO = V_PLAN_PRESUPUESTAL.DESTINO
    LEFT JOIN CLASETIPOORDENDECOMPRA ON
    CLASETIPOORDENDECOMPRA.CODIGO = TIPOORDENDECOMPRA.CODCONTTOLIMA
s$condicion$s
GROUP BY 
    ORDENDECOMPRA.COMPANIA,
    ORDENDECOMPRA.CLASEORDEN, 
    CASE TIPOORDENDECOMPRA.CONVENIO WHEN 0 THEN 'LEY 80' ELSE 'CONVENIOS LEY 489' END, 
    'MUNICIPAL',
    0 ,
    ORDENDECOMPRA.NUMERO, 
    TIPOADJUDICACION.EQUIVALENTESIA, 
    TIPOADJUDICACION.PROCEDIMIENTOSIA , 
    TIPOORDENDECOMPRA.TIPOCONTRATO_SIA,
    'FUNCIONAMIENTO', 
    SECTORES.SECTOR_SIA, 
    ORDENDECOMPRA.VALORTOTAL, 
    ORDENDECOMPRA.NOMBRECONTRATISTA,
    ORDENDECOMPRA.TERCERO, 
    CASE TERCERO.NATURALEZA WHEN 'J' THEN 'JURIDICA' ELSE 'NATURAL' END, 
    RUBROCONTRATO_FUENTESR.CUENTA,
    CASE ORDENDECOMPRA.FECHASUSCRIPCION WHEN  NULL THEN ORDENDECOMPRA.FECHA ELSE ORDENDECOMPRA.FECHASUSCRIPCION END, 
    ORDENDECOMPRA.INTERVENTOR, 
    ORDENDECOMPRA.CEDULAINTERVENTOR, 
    CASE ORDENDECOMPRA.TIPOINTERVENTOR WHEN 'I' THEN 'INTERNO' ELSE 'EXTERNO' END , 
    'DIAS', 
    ORDENDECOMPRA.PLAZODEENTREGA, 
    CASE ABONOS WHEN 0 THEN 'NO' ELSE 'SI' END,
    ORDENDECOMPRA.ABONOS,
    CASE ORDENDECOMPRA.FECHAINICIO WHEN  NULL THEN ORDENDECOMPRA.FECHA ELSE ORDENDECOMPRA.FECHAINICIO END, 
    ORDENDECOMPRA.FECHAFINALIZACION, 
    CASE ADICION_CONTRATOS.VALORADICION WHEN 0 THEN 'SI' ELSE 'NO' END ,
    NVL(VALORADICION,0),
    ORDENDECOMPRA.FECHALIQUIDACION, 
    CASE WHEN ORDENDECOMPRA.EMERGENCIAIN=0 OR ORDENDECOMPRA.EMERGENCIAIN IS NULL THEN 'NO' ELSE 'SI' END,
    RUBROCONTRATO_FUENTESR.NUMEROACTO, 
    RUBROCONTRATO_FUENTESR.FECHAACTO,
    RUBROCONTRATO_FUENTESR.VALORURGENCIA, 
    NVL(VALORPROPIOS,0),
    NVL(VALORREGALIAS,0), 
    NVL(VALORSGP,0), 
    NVL(COLHUMANITARIA,0),
    TIPOORDENDECOMPRA.CLASE,
    CASE RUBRO_VIGFUTURA.NUMEROCONTRATO WHEN NULL THEN 'NO' ELSE 'SI' END, 
    RUBRO_VIGFUTURA.FECHA_AUTO_VF, 
    RUBRO_VIGFUTURA.FECHA_AUTO_VF, 
    EXTRACT (YEAR FROM RUBRO_VIGFUTURA.FECHA_APROB_VF),
    ORDENDECOMPRA.FECHAPUBLICACION,
    TIPOORDENDECOMPRA.CODCONTTOLIMA,
    V_RESUMENPPTO_BASE.ADICIONES, V_RESUMENPPTO_BASE.REDUCCIONES,
    TIPODESTINO.NOMBRE,
    V_RESUMENPPTO_BASE.TIPOVIGENCIA,
    
ORDENDECOMPRA.FECHAFIRMA,
    ORDENDECOMPRA.FECHA,
    ORDENDECOMPRA.VALOR_ANTICIPO,
    CLASETIPOORDENDECOMPRA.SIAF20
