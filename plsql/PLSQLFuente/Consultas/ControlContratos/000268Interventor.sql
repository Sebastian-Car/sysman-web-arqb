SELECT ORDENDECOMPRA.CLASEORDEN,
  ORDENDECOMPRA.NUMERO,
  ORDENDECOMPRA.INTERVENTOR,
  ORDENDECOMPRA.CARGOINTERVENTOR,
  ORDENDECOMPRA.OBJETOCONTRATO,
  ORDENDECOMPRA.NOMBRECONTRATISTA,
  ORDENDECOMPRA.DURACION,
  TO_CHAR(ORDENDECOMPRA.FECHAINICIO,'DD/MM/YYYY') FECHAINICIO,
  TO_CHAR(ORDENDECOMPRA.FECHAFINALIZACION ,'DD/MM/YYYY') FECHAFINALIZACION,
  ORDENDECOMPRA.VALORTOTAL,
  ORDENDECOMPRA.VALORPAGOS,
  ORDENDECOMPRA.ESTADO
FROM ORDENDECOMPRA
INNER JOIN TERCERO
ON ORDENDECOMPRA.COMPANIA    = TERCERO.COMPANIA
AND ORDENDECOMPRA.TERCERO    = TERCERO.NIT
AND ORDENDECOMPRA.SUCURSAL   = TERCERO.SUCURSAL
WHERE 
ORDENDECOMPRA.COMPANIA = 's$compania$s' AND ORDENDECOMPRA.INTERVENTOR 
BETWEEN 's$terceroInicial$s' AND 's$terceroFinal$s' AND TRUNC(ORDENDECOMPRA.FECHAINICIO) BETWEEN s$fechaInicial$s AND s$fechaFinal$s
AND -1 IN (
  CASE
    WHEN 's$estado$s'           = 'Z'
    OR 
ORDENDECOMPRA.ESTADO = 's$estado$s'   THEN -1
    ELSE 0
  END)
ORDER BY TERCERO.NOMBRE
