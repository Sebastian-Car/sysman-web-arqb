SELECT ORDENDECOMPRA.COMPANIA,
       TERCERO.NOMBRE,
       TERCERO.NIT TERCERO,
       UPPER(ORDENDECOMPRA.CLASEORDEN) CLASE_ORDEN,
       ORDENDECOMPRA.NUMERO,
       ORDENDECOMPRA.OBJETOCONTRATO,
       NVL(ORDENDECOMPRA.PLAZODEENTREGA,0) || ' días' PLAZO,
       ORDENDECOMPRA.VALORTOTAL,
       TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') FECHA,
       TO_CHAR(ORDENDECOMPRA.FECHAFINALIZACION,'DD/MM/YYYY') FECHAFINALIZACION,
       TO_CHAR(ORDENDECOMPRA.FECHALIQUIDACION,'DD/MM/YYYY') FECHALIQUIDACION,
       LISTAGG(SUPERVISOR.NOMBRE,',') WITHIN GROUP(ORDER  BY SUPERVISOR.NOMBRE) SUPERVISORES,
       DECODE(ORDENDECOMPRA.ESTADO,'V','Vigente','L','Liquidado','T','Terminado','S','Suspendido','A','Anulado','','') ESTADO,
       TO_CHAR(SYSDATE,'DD "de" FMMONTH "del" YYYY') FECHA_ACTUAL
FROM   ORDENDECOMPRA
INNER  JOIN TIPOORDENDECOMPRA 
   ON  ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA
  AND  ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO
 LEFT  JOIN TERCERO
   ON  ORDENDECOMPRA.COMPANIA  = TERCERO.COMPANIA
  AND  ORDENDECOMPRA.TERCERO   = TERCERO.NIT
  AND  ORDENDECOMPRA.SUCURSAL  = TERCERO.SUCURSAL
 LEFT  JOIN SUPERVISORES
   ON  ORDENDECOMPRA.COMPANIA   = SUPERVISORES.COMPANIA
  AND  ORDENDECOMPRA.CLASEORDEN = SUPERVISORES.CLASEORDEN
  AND  ORDENDECOMPRA.NUMERO     = SUPERVISORES.NUMEROCONTRATO
 LEFT  JOIN TERCERO SUPERVISOR
   ON  SUPERVISORES.COMPANIA  = SUPERVISOR.COMPANIA
  AND  SUPERVISORES.CEDULA    = SUPERVISOR.NIT
  AND  SUPERVISORES.SUCURSAL  = SUPERVISOR.SUCURSAL
WHERE  ORDENDECOMPRA.COMPANIA = s$compania$s
  AND  UPPER(ORDENDECOMPRA.CLASEORDEN) BETWEEN 's$tipoContratoInicial$s' AND 's$tipoContratoFinal$s'
  AND  ORDENDECOMPRA.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s 
  AND  TIPOORDENDECOMPRA.CLASE IN ('C')
  AND  TERCERO.NOMBRE BETWEEN 's$nombreTerceroInicial$s' AND 's$nombreTerceroFinal$s' 
GROUP  BY ORDENDECOMPRA.COMPANIA,
       TERCERO.NOMBRE,
       TERCERO.NIT,
       UPPER(ORDENDECOMPRA.CLASEORDEN),
       ORDENDECOMPRA.NUMERO,
       ORDENDECOMPRA.OBJETOCONTRATO,
       NVL(ORDENDECOMPRA.PLAZODEENTREGA,0),
       ORDENDECOMPRA.VALORTOTAL,
        TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY'),
       TO_CHAR(ORDENDECOMPRA.FECHAFINALIZACION,'DD/MM/YYYY'),
       TO_CHAR(ORDENDECOMPRA.FECHALIQUIDACION,'DD/MM/YYYY'),
       DECODE(ORDENDECOMPRA.ESTADO,'V','Vigente','L','Liquidado','T','Terminado','S','Suspendido','A','Anulado','',''),
       TO_CHAR(SYSDATE,'DD "de" FMMONTH "del" YYYY')
ORDER  BY TERCERO.NOMBRE, ORDENDECOMPRA.NUMERO
