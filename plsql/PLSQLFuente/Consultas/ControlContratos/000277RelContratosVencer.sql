SELECT 
  ORDENDECOMPRA.COMPANIA,
  TERCERO.NOMBRE NOMBRETERCERO, 
  ORDENDECOMPRA.CLASEORDEN, 
  ORDENDECOMPRA.NUMERO, 
  TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') FECHA,
  TO_CHAR(ORDENDECOMPRA.FECHAFINALIZACION,'DD/MM/YYYY') FECHAFINALIZACION,
  CASE WHEN ORDENDECOMPRA.ESTADO = 'V' 
    THEN 'Vigente' END ESTADO, 
  ORDENDECOMPRA.OBJETOCONTRATO, 
  ORDENDECOMPRA.VALORTOTAL, 
  TIPOORDENDECOMPRA.NOMBRE NOMBRETIPO,
  ORDENDECOMPRA.INTERVENTOR
FROM ORDENDECOMPRA 
  LEFT JOIN TIPOORDENDECOMPRA 
      ON  ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA
    AND ORDENDECOMPRA.CLASEORDEN   = TIPOORDENDECOMPRA.CODIGO
  LEFT JOIN TERCERO 
    ON  ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA
    AND ORDENDECOMPRA.TERCERO  = TERCERO.NIT
    AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL
WHERE ORDENDECOMPRA.COMPANIA = s$compania$s
  AND TERCERO.NIT  BETWEEN 's$terceroInicial$s' AND 's$terceroFinal$s' 
  AND ORDENDECOMPRA.ESTADO = 'V' 
  AND TIPOORDENDECOMPRA.CODIGO BETWEEN 's$contratoIncial$s' AND 's$contratoFinal$s'
  AND ORDENDECOMPRA.FECHAFINALIZACION BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND NVL(TIPOORDENDECOMPRA.CLASE,'')<>'M'
  AND FECHALIQUIDACION IS NULL
ORDER BY ORDENDECOMPRA.COMPANIA, 
  ORDENDECOMPRA.CLASEORDEN, 
  ORDENDECOMPRA.NUMERO