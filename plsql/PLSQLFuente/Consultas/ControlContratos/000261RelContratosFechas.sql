SELECT DISTINCT
  ORDENDECOMPRA.COMPANIA,
'' SUPERVISOR,
  ORDENDECOMPRA.INTERVENTOR,
  ORDENDECOMPRA.CLASEORDEN,
  TIPOORDENDECOMPRA.NOMBRE NOMBRETIPO, 
  ORDENDECOMPRA.NUMERO,
  TERCERO.NOMBRE NOMBRETERCERO,
  TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY')FECHA,
  TO_CHAR(ORDENDECOMPRA.FECHAFINALIZACION,'DD/MM/YYYY')FECHAFINALIZACION,
  ORDENDECOMPRA.OBJETOCONTRATO,
  CASE WHEN ORDENDECOMPRA.ESTADO = 'V' THEN 'Vigente' ELSE
    CASE WHEN ORDENDECOMPRA.ESTADO = 'A' THEN 'Anulado' ELSE 
      CASE WHEN ORDENDECOMPRA.ESTADO = 'L' THEN 'Liquidado' ELSE 
        CASE WHEN ORDENDECOMPRA.ESTADO = 'T' THEN 'Terminado' ELSE 
          CASE WHEN ORDENDECOMPRA.ESTADO = 'S' THEN 'Suspendido' ELSE 
            CASE WHEN ORDENDECOMPRA.ESTADO = 'P' THEN 'En Proceso de Sanción o Proceso Judicial' ELSE 'Todos'
             END
           END
         END
       END
     END
   END ESTADO,
   ORDENDECOMPRA.VALORTOTAL,
   NVL(SUMADEVALORTOTAL,0) ADICIONES,
   DEPENDENCIA.NOMBRE NOMBREDEPENDENCIA
FROM ORDENDECOMPRA 
  LEFT JOIN TERCERO 
    ON ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA
    AND ORDENDECOMPRA.TERCERO = TERCERO.NIT 
    AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL
  LEFT JOIN (SELECT ORDENDECOMPRA.COMPANIA, 
                    ORDENDECOMPRA.TIPOAFECTADO, 
                    ORDENDECOMPRA.NUMEROAFECTADO, 
                    SUM(ORDENDECOMPRA.VALORTOTAL) SUMADEVALORTOTAL
             FROM  ORDENDECOMPRA
             GROUP BY ORDENDECOMPRA.COMPANIA, 
                      ORDENDECOMPRA.TIPOAFECTADO,
                      ORDENDECOMPRA.NUMEROAFECTADO) ADICIONESCONTRATOS 
    ON ORDENDECOMPRA.COMPANIA = ADICIONESCONTRATOS.COMPANIA 
    AND ORDENDECOMPRA.CLASEORDEN = ADICIONESCONTRATOS.TIPOAFECTADO 
    AND ORDENDECOMPRA.NUMERO = ADICIONESCONTRATOS.NUMEROAFECTADO 
  INNER JOIN TIPOORDENDECOMPRA 
    ON ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA
    AND ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO 
  LEFT JOIN DEPENDENCIA ON ORDENDECOMPRA.COMPANIA = DEPENDENCIA.COMPANIA 
    AND ORDENDECOMPRA.DEPENDENCIA = DEPENDENCIA.CODIGO
  LEFT JOIN SUPERVISORES
  ON SUPERVISORES.COMPANIA=ORDENDECOMPRA.COMPANIA
WHERE ORDENDECOMPRA.COMPANIA = s$compania$s
  AND ORDENDECOMPRA.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND TERCERO.NOMBRE BETWEEN s$nombreInicial$s AND s$nombreFinal$s
  AND TIPOORDENDECOMPRA.NOMBRE BETWEEN s$contratoInicial$s AND s$contratoFinal$s
  AND ORDENDECOMPRA.DEPENDENCIA BETWEEN s$dependenciaInicial$s AND s$dependenciaFinal$s
  AND NVL(ORDENDECOMPRA.ESTADO,'') NOT IN ('A')
ORDER BY ORDENDECOMPRA.COMPANIA, 
  ORDENDECOMPRA.CLASEORDEN, 
  ORDENDECOMPRA.NUMERO