SELECT TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') FECHA,
            TERCERO.NIT, 
            ORDENDECOMPRA.NUMERO,  
            ORDENDECOMPRA.OBJETOCONTRATO,  
            ORDENDECOMPRA.CLASE,  
            ROUND(ORDENDECOMPRA.VALORTOTAL, - 3) / 1000  VALOR_CONTRATO,  
            TERCERO.NOMBRE,  
            TERCERO.NIT,  
            V_ORDENDECOMPRA_DIS.COMPROBANTES DISPONIBILIDAD,  
            V_ORDENDECOMPRA_DIS.FECHACDP FECHADEDISPONIBILIDAD,  
            TO_CHAR(ORDENDECOMPRA.FECHAFIRMA,'DD/MM/YYYY') FECHAFIRMA,  
            ORDENDECOMPRA.LICITACIONOCONCURSO,  
            TIPOORDENDECOMPRA.GENERACONTRATO  CONFORMALIDADESPLENAS,  
            CASE WHEN  
                TIPOORDENDECOMPRA.GENERACONTRATO = 0 AND(ORDENDECOMPRA.CLASEORDEN = 'ODC' OR ORDENDECOMPRA.CLASEORDEN = 'ODI') 
            THEN  
                - 1 
            ELSE 
                0 
            END  ORDENCOM,  
            CASE WHEN  
                TIPOORDENDECOMPRA.GENERACONTRATO = 0 AND ORDENDECOMPRA.CLASEORDEN = 'ODS' 
            THEN  
                - 1 
            ELSE 
                0 
            END  ORDENSUMI,  
            V_ORDENDECOMPRA_RES.FECHACDP FECHAREGISTRO,  
            V_ORDENDECOMPRA_RES.COMPROBANTES RESERVA,  
            ROUND( V_ORDENDECOMPRA_RES.TOTAL_RES, - 3) / 1000  VALOR_RESERVA,  
            V_ORDENDECOMPRA_RUBRO.RUBRO RUBRORESERVA,  
            TO_CHAR(ORDENDECOMPRA.FECHAPOLIZAS,'DD/MM/YYYY') FECHAPOLIZAS, 
            TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') FECHA_INICIO, 
            ORDENDECOMPRA.DURACION, 
            TO_CHAR(NOVEDADCONTRATO.FECHA,'DD/MM/YYYY') FECHA_NOVEDAD, 
            TO_CHAR(NOVEDADCONTRATO.FECHAVENCIMIENTO,'DD/MM/YYYY') FECHAVENCIMIENTO, 
            ROUND(NOVEDADCONTRATO.VALORTOTAL, - 3) / 1000 VALORTOTAL_NOVEDAD,  
            ROUND(ORDENDECOMPRA.VALORPAGOS, - 3) / 1000 VALORPAGOS,  
            TO_CHAR(ORDENDECOMPRA.FECHAFINALIZACION,'DD/MM/YYYY') FECHAFINALIZACION,  
            TO_CHAR(ORDENDECOMPRA.FECHALIQUIDACION,'DD/MM/YYYY')FECHALIQUIDACION
        FROM  
            ( 
                ( 
                    ORDENDECOMPRA  
                    LEFT JOIN NOVEDADCONTRATO  
                    ON (ORDENDECOMPRA.COMPANIA = NOVEDADCONTRATO.COMPANIA)  
                    AND (ORDENDECOMPRA.CLASEORDEN = NOVEDADCONTRATO.CLASEORDEN)  
                    AND (ORDENDECOMPRA.NUMERO = NOVEDADCONTRATO.ORDENDECOMPRA) 
                )  
                LEFT JOIN TERCERO  
                ON (ORDENDECOMPRA.TERCERO = TERCERO.NIT)  
                AND (ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL)  
                AND (ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA) 
            )  
            LEFT JOIN TIPOORDENDECOMPRA  
            ON (ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA)  
            AND (ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO)  
             LEFT JOIN V_ORDENDECOMPRA_DIS  
                    ON (ORDENDECOMPRA.COMPANIA = V_ORDENDECOMPRA_DIS.COMPANIA)  
                    AND (ORDENDECOMPRA.CLASEORDEN = V_ORDENDECOMPRA_DIS.CLASEORDEN)  
                    AND (ORDENDECOMPRA.NUMERO = V_ORDENDECOMPRA_DIS.NUMERO) 
            LEFT JOIN V_ORDENDECOMPRA_RES 
              ON (ORDENDECOMPRA.COMPANIA = V_ORDENDECOMPRA_RES.COMPANIA)  
              AND (ORDENDECOMPRA.CLASEORDEN = V_ORDENDECOMPRA_RES.CLASEORDEN)  
              AND (ORDENDECOMPRA.NUMERO = V_ORDENDECOMPRA_RES.NUMERO)               
            LEFT JOIN V_ORDENDECOMPRA_RUBRO 
              ON (ORDENDECOMPRA.COMPANIA = V_ORDENDECOMPRA_RUBRO.COMPANIA)  
              AND (ORDENDECOMPRA.CLASEORDEN = V_ORDENDECOMPRA_RUBRO.CLASEORDEN)  
              AND (ORDENDECOMPRA.NUMERO = V_ORDENDECOMPRA_RUBRO.NUMERO)            
        WHERE ORDENDECOMPRA.COMPANIA = s$compania$s
            AND ORDENDECOMPRA.FECHA Between s$fechainicial$s And s$fechafinal$s
            AND TERCERO.NIT = 's$tercero$s' 
            AND NVL(TIPOORDENDECOMPRA.CLASE, '') <> 'M'  
            AND ORDENDECOMPRA.ESTADO <> 'A'