SELECT DISTINCT 2 AS TIPOREGISTRO,
       83 AS CLASEINFORMACION,
       TIPOORDENDECOMPRA.CODDIAN AS TIPOCONTRATO,
       ORDENDECOMPRA.TERCERO AS TERCERO,
       TERCERO.TIPOID AS TIPODOC,
       TERCERO.NOMBRE AS NOMBRERAZONSOCIAL,
       TERCERO.DIRECCION AS DIRECCION,
       TERCERO.DEPARTAMENTO || TERCERO.CIUDAD AS CODDEPTOMPIO,
       TO_CHAR(ORDENDECOMPRA.NUMERO) AS NUMEROCONTRATO,
       ORDENDECOMPRA.VALORTOTAL,
       NVL(PAGOS,0)AS VALORPAGOS,
       (TO_CHAR(ORDENDECOMPRA.FECHA,'YYYYMMDD')) AS FECHACONTRATO
FROM ORDENDECOMPRA 
  LEFT JOIN TIPOORDENDECOMPRA 
    ON ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA 
    AND ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO 
  LEFT JOIN TERCERO 
    ON ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA 
    AND ORDENDECOMPRA.TERCERO = TERCERO.NIT 
    AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL 
  LEFT JOIN ORDENDECOMPRAPPTO 
    ON ORDENDECOMPRA.COMPANIA = ORDENDECOMPRAPPTO.COMPANIA
    AND ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRAPPTO.CLASEORDEN
    AND ORDENDECOMPRA.NUMERO = ORDENDECOMPRAPPTO.NUMERO
  LEFT JOIN (SELECT NOVEDADCONTRATO.COMPANIA, 
                     NOVEDADCONTRATO.CLASEORDEN, 
                     NOVEDADCONTRATO.ORDENDECOMPRA, 
                     SUM(NOVEDADCONTRATO.VALORTOTAL) AS PAGOS
             FROM NOVEDADCONTRATO 
               LEFT JOIN TIPO_COMPROBANTE 
               ON NOVEDADCONTRATO.COMPANIA = TIPO_COMPROBANTE.COMPANIA
               AND NOVEDADCONTRATO.TIPOT = TIPO_COMPROBANTE.CODIGO 
             WHERE NOVEDADCONTRATO.COMPANIA = s$compania$s 
               AND (TO_CHAR(NOVEDADCONTRATO.FECHA , 'YYYY'))= s$ano$s
               AND NOVEDADCONTRATO.CLASET='C'  
               AND TIPO_COMPROBANTE.CLASE_CONTABLE ='E'
            GROUP BY NOVEDADCONTRATO.COMPANIA, 
               NOVEDADCONTRATO.CLASEORDEN, 
               NOVEDADCONTRATO.ORDENDECOMPRA)
  DIANPAGOSXCONTRATO 
    ON  ORDENDECOMPRA.COMPANIA = DIANPAGOSXCONTRATO.COMPANIA  
    AND ORDENDECOMPRA.CLASEORDEN = DIANPAGOSXCONTRATO.CLASEORDEN 
    AND ORDENDECOMPRA.NUMERO = DIANPAGOSXCONTRATO.ORDENDECOMPRA
  LEFT JOIN (SELECT  ORDENDECOMPRA.COMPANIA, 
                     ORDENDECOMPRA.CLASEORDEN, 
                     ORDENDECOMPRA.NUMERO, 
                     ORDENDECOMPRA.TERCERO, 
                     SUM(ORDENDECOMPRA.VALORTOTAL) AS CONTRATADO, 
                     TIPOORDENDECOMPRA.CODDIAN, TERCERO.NATURALEZA
              FROM ORDENDECOMPRA 
              LEFT JOIN TIPOORDENDECOMPRA 
                ON ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO 
                AND ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA 
              LEFT JOIN TERCERO 
                ON ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL 
                AND ORDENDECOMPRA.TERCERO = TERCERO.NIT 
                AND ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA
             WHERE ORDENDECOMPRA.COMPANIA = s$compania$s    
                AND TIPOORDENDECOMPRA.CODDIAN ='73' 
                AND TERCERO.NATURALEZA = 'N'    
            GROUP BY 
              ORDENDECOMPRA.COMPANIA, 
              ORDENDECOMPRA.CLASEORDEN, 
              ORDENDECOMPRA.NUMERO, 
              ORDENDECOMPRA.TERCERO, 
              TIPOORDENDECOMPRA.CODDIAN, 
              TERCERO.NATURALEZA
            HAVING SUM(ORDENDECOMPRA.VALORTOTAL)>10000000 
              AND TIPOORDENDECOMPRA.CODDIAN ='73' 
              AND TERCERO.NATURALEZA = 'N')
  DIANTOTALXSERVICIOSXTERCERO 
    ON ORDENDECOMPRA.COMPANIA = DIANTOTALXSERVICIOSXTERCERO.COMPANIA
    AND ORDENDECOMPRA.CLASEORDEN = DIANTOTALXSERVICIOSXTERCERO.CLASEORDEN 
    AND ORDENDECOMPRA.NUMERO = DIANTOTALXSERVICIOSXTERCERO.NUMERO 
  WHERE ORDENDECOMPRA.COMPANIA = s$compania$s
    AND TIPOORDENDECOMPRA.CODDIAN IS NOT NULL 
    AND TIPOORDENDECOMPRA.CODDIAN <>'73' 
    AND ORDENDECOMPRA.ESTADO <> 'A' 
    AND (TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY')) = s$ano$s
    OR TIPOORDENDECOMPRA.CODDIAN ='73' 
    AND DIANTOTALXSERVICIOSXTERCERO.NUMERO IS NOT NULL 
    AND (TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY')) = s$ano$s
