SELECT    
          ORDENDECOMPRA.COMPANIA,
          TERCERO.NOMBRE NOMBRETERCERO, 
          ORDENDECOMPRA.CLASEORDEN, 
          ORDENDECOMPRA.NUMERO NUMEROCONTRATO, 
          s$disponibilidad$s 
          ORDENDECOMPRA.VALORFINAL, 
          SUBSTR(OBJETOCONTRATO,1,255) OBJETOCONTRATOF,  
          ORDENDECOMPRA.VALORTOTALOP, 
          ORDENDECOMPRA.PLAZODEENTREGA, 
          ORDENDECOMPRA.TERCERO, 
          TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') FECHA, 
          TIPOORDENDECOMPRA.NOMBRE NOMBRETIPO, 
          ORDENDECOMPRA.VALORTOTAL-VALORFINAL ADICIONES, 
          (CASE ORDENDECOMPRA.ESTADO  WHEN 'V' THEN 'Vigente'  
                                      WHEN 'A' THEN 'Anulado'  
                                      WHEN 'L' THEN 'Liquidado'  
                                      WHEN 'T' THEN 'Terminado'  
                                      WHEN 'S' THEN 'Suspendido'  
                                      WHEN 'P' THEN 'En Proceso de Sanción o Proceso Judicial' ELSE 'Todos'
                    END)ESTADO, 
          TO_CHAR(ORDENDECOMPRA.FECHAFINALIZACION,'DD/MM/YYYY') FECHAFINALIZACION, 
          ORDENDECOMPRA.INTERVENTOR, 
          ORDENDECOMPRA.CARGOINTERVENTOR, 
          ORDENDECOMPRA.CEDULAINTERVENTOR, 
          ORDENDECOMPRA.VALORTOTAL, 
          (NVL(VALORPAGOS,0)) PAGOS, 
          ORDENDECOMPRA.INTERVENTOR, 
          TERCEROSUPER.NOMBRE NOMBRESUPER, 
          SUM(NOVEDADCONTRATO.VALOR_LIBERADO) SUMADEVALOR_LIBERADO,
          ORDENDECOMPRA.VALORFINAL + (ORDENDECOMPRA.VALORTOTAL-ORDENDECOMPRA.VALORFINAL) VALFINAL, 
          (ORDENDECOMPRA.VALORPAGOS/(ORDENDECOMPRA.VALORTOTAL+ (ORDENDECOMPRA.VALORTOTAL-ORDENDECOMPRA.VALORFINAL)))*100 EJECUTADO,
          100-((ORDENDECOMPRA.VALORPAGOS/(ORDENDECOMPRA.VALORTOTAL+(ORDENDECOMPRA.VALORTOTAL-ORDENDECOMPRA.VALORFINAL)))*100) SINEJECUTAR,
          SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO) VALOR_DEBITO
FROM ORDENDECOMPRA 
  LEFT JOIN TERCERO 
        ON  ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA 
        AND ORDENDECOMPRA.TERCERO  = TERCERO.NIT 
    AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL         
  INNER JOIN TIPOORDENDECOMPRA 
               ON ORDENDECOMPRA.COMPANIA   = TIPOORDENDECOMPRA.COMPANIA
                      AND ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO 
                LEFT JOIN SUPERVISORES 
                       ON ORDENDECOMPRA.COMPANIA   = SUPERVISORES.COMPANIA 
                      AND ORDENDECOMPRA.CLASEORDEN = SUPERVISORES.CLASEORDEN 
                      AND ORDENDECOMPRA.NUMERO     = SUPERVISORES.NUMEROCONTRATO
                LEFT JOIN TERCERO TERCEROSUPER
                    ON  TERCEROSUPER.COMPANIA  =   SUPERVISORES.COMPANIA
                    AND TERCEROSUPER.NIT              =  SUPERVISORES.CEDULA
                    AND TERCEROSUPER.SUCURSAL  =  SUPERVISORES.SUCURSAL 
                LEFT JOIN NOVEDADCONTRATO 
                       ON ORDENDECOMPRA.COMPANIA   = NOVEDADCONTRATO.COMPANIA 
                      AND ORDENDECOMPRA.CLASEORDEN = NOVEDADCONTRATO.CLASEORDEN 
                      AND ORDENDECOMPRA.NUMERO     = NOVEDADCONTRATO.ORDENDECOMPRA 
                LEFT JOIN ORDENDECOMPRAPPTO
                      ON  ORDENDECOMPRA.COMPANIA    = ORDENDECOMPRAPPTO.COMPANIA
                      AND ORDENDECOMPRA.CLASEORDEN  = ORDENDECOMPRAPPTO.CLASEORDEN
                      AND ORDENDECOMPRA.NUMERO      = ORDENDECOMPRAPPTO.NUMERO
               LEFT JOIN DETALLE_COMPROBANTE_PPTAL 
                      ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = ORDENDECOMPRAPPTO.COMPANIA
                      AND DETALLE_COMPROBANTE_PPTAL.ANO       = ORDENDECOMPRAPPTO.ANOPPTO
                      AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = ORDENDECOMPRAPPTO.TIPOPPTO
                      AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = ORDENDECOMPRAPPTO.NUMEROPPTO
                      AND DETALLE_COMPROBANTE_PPTAL.CUENTA      = ORDENDECOMPRAPPTO.RUBRO  
           WHERE ORDENDECOMPRA.FECHA BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY') 
           AND TIPOORDENDECOMPRA.NOMBRE BETWEEN 's$tipoContratoInicial$s' AND 's$tipoContratoFinal$s' 
           AND ORDENDECOMPRA.COMPANIA = s$compania$s 
           AND TERCERO.NIT BETWEEN 's$terceroIni$s' AND 's$terceroFin$s' 
           AND NVL(TIPOORDENDECOMPRA. CLASE,'')<>'M' 
           AND (ORDENDECOMPRAPPTO.TIPOPPTO IN ('DIS') OR ORDENDECOMPRAPPTO.TIPOPPTO IS NULL)
           AND (ORDENDECOMPRA.VALORTOTAL+(ORDENDECOMPRA.VALORTOTAL-ORDENDECOMPRA.VALORFINAL)) <> 0
           s$strWhere$s 
GROUP BY ORDENDECOMPRA.COMPANIA,         TERCERO.NOMBRE,         ORDENDECOMPRA.CLASEORDEN,         ORDENDECOMPRA.NUMERO,          s$disponibilidadg$s
         ORDENDECOMPRA.VALORFINAL,         SUBSTR(OBJETOCONTRATO, 1, 255),         ORDENDECOMPRA.VALORTOTALOP,
         ORDENDECOMPRA.PLAZODEENTREGA,         ORDENDECOMPRA.TERCERO,
         TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY'),         TIPOORDENDECOMPRA.NOMBRE,
         ORDENDECOMPRA.VALORTOTAL - VALORFINAL,         ORDENDECOMPRA.ESTADO,
         TO_CHAR(ORDENDECOMPRA.FECHAFINALIZACION,'DD/MM/YYYY'),         ORDENDECOMPRA.INTERVENTOR,
         ORDENDECOMPRA.CARGOINTERVENTOR,         ORDENDECOMPRA.CEDULAINTERVENTOR,
         ORDENDECOMPRA.VALORTOTAL,         (NVL(VALORPAGOS, 0)),
         ORDENDECOMPRA.INTERVENTOR,          TERCEROSUPER.NOMBRE,         ORDENDECOMPRA.VALORFINAL,
         (ORDENDECOMPRA.VALORPAGOS / ( ORDENDECOMPRA.VALORTOTAL + ( ORDENDECOMPRA.VALORTOTAL - ORDENDECOMPRA.VALORFINAL))) * 100,
         100 - ((ORDENDECOMPRA.VALORPAGOS / (ORDENDECOMPRA.VALORTOTAL + (ORDENDECOMPRA.VALORTOTAL - ORDENDECOMPRA.VALORFINAL))) * 100)
           ORDER BY ORDENDECOMPRA.CLASEORDEN