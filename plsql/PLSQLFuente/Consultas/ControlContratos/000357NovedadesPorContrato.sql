SELECT  
    ORDENDECOMPRA.CLASEORDEN,
    TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') AS OFECHA, 
    NOVEDADCONTRATO.TIPOT,
    CLASETRANSACCIONC.NOMBRE AS CTNOMBRE, 
    NOVEDADCONTRATO.NUMERO AS NNUMERO, 
    NOVEDADCONTRATO.VALORTOTAL AS NVALORTOTAL, 
    TERCERO.NOMBRE AS TRNOMBRE,
    TO_CHAR(NOVEDADCONTRATO.FECHA,'DD/MM/YYYY') AS NFECHA,
    ORDENDECOMPRA.VALORTOTAL AS OVALORTOTAL,
   ORDENDECOMPRA.NUMERO AS ONUMERO,
    CLASENOVEDAD.NOMBRE AS CNNOMBRE
FROM        (
                (ORDENDECOMPRA 
                    LEFT JOIN TERCERO ON 
                        ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA AND 
                        ORDENDECOMPRA.TERCERO = TERCERO.NIT AND 
                        ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL
                ) 
                LEFT JOIN 
                    (NOVEDADCONTRATO 
                        LEFT JOIN CLASETRANSACCIONC ON 
                            NOVEDADCONTRATO.TIPOT = CLASETRANSACCIONC.TIPOT AND 
                            NOVEDADCONTRATO.CLASET = CLASETRANSACCIONC.CLASET
                    ) 
                ON 
                    ORDENDECOMPRA.COMPANIA = NOVEDADCONTRATO.COMPANIA AND 
                    ORDENDECOMPRA.CLASEORDEN = NOVEDADCONTRATO.CLASEORDEN AND 
                    ORDENDECOMPRA.NUMERO = NOVEDADCONTRATO.ORDENDECOMPRA
            ) 
            LEFT JOIN TIPOORDENDECOMPRA ON 
                ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA AND 
                ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO 
    LEFT JOIN CLASENOVEDAD 
    ON CLASETRANSACCIONC.CLASENOVEDAD = CLASENOVEDAD.CODIGO 
WHERE   ORDENDECOMPRA.COMPANIA          = s$compania$s
  AND   ORDENDECOMPRA.CLASEORDEN        = 's$claseOrden$s' 
  AND   ORDENDECOMPRA.NUMERO            =  s$numero$s
  AND   CLASETRANSACCIONC.CLASENOVEDAD  = 's$claseNovedad$s'  
ORDER BY 
    ORDENDECOMPRA.COMPANIA, 
    ORDENDECOMPRA.CLASEORDEN, 
    ORDENDECOMPRA.NUMERO