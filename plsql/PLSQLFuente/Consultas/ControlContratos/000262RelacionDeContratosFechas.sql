SELECT ORDENDECOMPRA.CLASEORDEN,  
       ORDENDECOMPRA.NUMERO,  
       TERCERO.NOMBRE, 
       TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') FECHA,  
       NVL(C_DISPCONTRATOFECHAS.VLR_DOCUMENTO,0)  VALORDISPON, 
       NVL(C_ADICIONESCONTRATOFECHA.VLR_DOCUMENTO,0)  VALORADICION,  
       ORDENDECOMPRA.COMPANIA, 
       C_PAGOSCONTRATOFECHAS.NUMERO PAGONO,  
       NVL(C_PAGOSCONTRATOFECHAS.VALORPAGOS,0)  TOTALPAGO,  
       TO_CHAR(C_PAGOSCONTRATOFECHAS.FECHA,'DD/MM/YYYY') FECHAPAGO,  
       NVL(C_DISPCONTRATOFECHAS.VLR_DOCUMENTO- C_PAGOSCONTRATOFECHAS.VALORPAGOS,0)  SALDO 
FROM ORDENDECOMPRA  
  LEFT JOIN TERCERO  
    ON ORDENDECOMPRA.COMPANIA  = TERCERO.COMPANIA   
    AND ORDENDECOMPRA.TERCERO  = TERCERO.NIT  
    AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL  
  LEFT JOIN (SELECT COMPROBANTE_CNT.COMPANIA,  
        					  COMPROBANTE_CNT.TIPO,  
         					  COMPROBANTE_CNT.NUMERO,  
         					  COMPROBANTE_CNT.FECHA,  
         					  COMPROBANTE_CNT.VLRAGIRAR VALORPAGOS,  
        					  COMPROBANTE_CNT.TIPOCONTRATO,  
         					  COMPROBANTE_CNT.NUMEROCONTRATO,  
         					  COMPROBANTE_CNT.IND_ANTICIPO 
     				 FROM COMPROBANTE_CNT  
               LEFT JOIN TIPO_COMPROBANTE  
                 ON  COMPROBANTE_CNT.COMPANIA   = TIPO_COMPROBANTE.COMPANIA  
    						 AND COMPROBANTE_CNT.TIPO       = TIPO_COMPROBANTE.CODIGO 
     			   WHERE COMPROBANTE_CNT.IND_ANTICIPO = 0) C_PAGOSCONTRATOFECHAS  
    ON  ORDENDECOMPRA.COMPANIA   = C_PAGOSCONTRATOFECHAS.COMPANIA  
    AND ORDENDECOMPRA.CLASEORDEN = C_PAGOSCONTRATOFECHAS.TIPOCONTRATO 
    AND ORDENDECOMPRA.NUMERO     = C_PAGOSCONTRATOFECHAS.NUMEROCONTRATO 
  LEFT JOIN (SELECT COMPROBANTE_PPTAL.COMPANIA,  
         					  COMPROBANTE_PPTAL.ANO,  
         					  COMPROBANTE_PPTAL.TIPO,  
        					  COMPROBANTE_PPTAL.NUMERO,  
         					  COMPROBANTE_PPTAL.FECHA,  
        					  TIPO_COMPROBPP.CLASE,  
        					  COMPROBANTE_PPTAL.VLR_DOCUMENTO,  
        					  COMPROBANTE_PPTAL.TIPOCONTRATO,  
         					  COMPROBANTE_PPTAL.NUMEROCONTRATO,  
        					  'DIS' AS TIPOADICION 
       				FROM COMPROBANTE_PPTAL  
      					LEFT JOIN TIPO_COMPROBPP  
          			   ON COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA   
                  AND COMPROBANTE_PPTAL.TIPO = TIPO_COMPROBPP.CODIGO) C_DISPCONTRATOFECHAS  
    ON  ORDENDECOMPRA.COMPANIA   = C_DISPCONTRATOFECHAS.COMPANIA 
    AND ORDENDECOMPRA.CLASEORDEN = C_DISPCONTRATOFECHAS.TIPOCONTRATO  
    AND ORDENDECOMPRA.NUMERO     = C_DISPCONTRATOFECHAS.NUMEROCONTRATO  
  LEFT JOIN ( SELECT 	COMPROBANTE_PPTAL.COMPANIA,  
                			COMPROBANTE_PPTAL.ANO,  
                			COMPROBANTE_PPTAL.TIPO,  
                 			COMPROBANTE_PPTAL.NUMERO,  
                 			COMPROBANTE_PPTAL.FECHA,  
                 			TIPO_COMPROBPP.CLASE,  
                 			COMPROBANTE_PPTAL.VLR_DOCUMENTO,  
                 			COMPROBANTE_PPTAL.TIPOCONTRATO,  
                  		COMPROBANTE_PPTAL.NUMEROCONTRATO,  
                    	'RES' TIPOADICION 
         			FROM COMPROBANTE_PPTAL  
           			LEFT JOIN TIPO_COMPROBPP  
         					ON  COMPROBANTE_PPTAL.TIPO     = TIPO_COMPROBPP.CODIGO  
           				AND COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA) C_ADICIONESCONTRATOFECHA  
    ON 	ORDENDECOMPRA.COMPANIA   = C_ADICIONESCONTRATOFECHA.COMPANIA  
    AND ORDENDECOMPRA.CLASEORDEN = C_ADICIONESCONTRATOFECHA.TIPOCONTRATO  
    AND ORDENDECOMPRA.NUMERO     = C_ADICIONESCONTRATOFECHA.NUMEROCONTRATO  
  LEFT JOIN TIPOORDENDECOMPRA  
    ON  ORDENDECOMPRA.COMPANIA   = TIPOORDENDECOMPRA.COMPANIA  
    AND ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO 
WHERE ORDENDECOMPRA.COMPANIA= s$compania$s 
  AND ORDENDECOMPRA.FECHA BETWEEN  s$fechaInicial$s   AND   s$fechaFinal$s    
  AND TIPOORDENDECOMPRA.CLASE NOT IN('M') 
ORDER BY ORDENDECOMPRA.COMPANIA,  
     	 	 ORDENDECOMPRA.CLASEORDEN
