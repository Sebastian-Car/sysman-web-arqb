
UPDATE CONSULTAS SET CONSULTA=TO_CLOB(q'#SELECT DISTINCT ORDENDECOMPRA.CLASEORDEN, 
       TIPOORDENDECOMPRA.NOMBRE  NOMBREORDEN, 
       TO_CHAR(ORDENDECOMPRA.NUMERO) NUMERO,  
       CASE TIPOORDENDECOMPRA.GENERACONTRATO WHEN -1 THEN 'x' WHEN 0 THEN ' ' END GENERACONTRATO,  
       ORDENDECOMPRA.FECHA,  
       TERCERO.NOMBRE BENEFICIARIO,  
       ORDENDECOMPRA.OBJETOCONTRATO OBJETO,  
       ORDENDECOMPRA.DURACION DURA,  
       ORDENDECOMPRA.VALORTOTAL VALORCONTRATO,  
       ORDENDECOMPRAPPTO.FUENTE_RECURSO ORIGENRECURSOS,  
       CASE ORDENDECOMPRA.ESTADO WHEN 'V' THEN 'Vigente' 
                                 WHEN 'A' THEN 'Anulada' 
                                 WHEN 'T' THEN 'Terminada' 
                                 WHEN 'L' THEN 'Liquidada' 
                                 WHEN 'P' THEN 'En proceso de sanción o proceso judicial' END ESTADO, 
       COMPROBANTE_CNT.FECHA FECHACOMPROBANTE,  
       COMPROBANTE_CNT.VLR_DOCUMENTO, 
       ORDENDECOMPRA.FECHALIQUIDACION 
FROM ORDENDECOMPRA  
  LEFT JOIN TERCERO  
    ON ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA  
    AND ORDENDECOMPRA.TERCERO = TERCERO.NIT  
    AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL  
  LEFT JOIN TIPOORDENDECOMPRA  
    ON ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA  
    AND ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO  
  LEFT JOIN COMPROBANTE_CNT  
    ON ORDENDECOMPRA.COMPANIA = COMPROBANTE_CNT.COMPANIA  
    AND ORDENDECOMPRA.NUMERO = COMPROBANTE_CNT.NUMERO  
    AND ORDENDECOMPRA.CLASEORDEN = COMPROBANTE_CNT.TIPO  
  LEFT JOIN TIPO_COMPROBANTE  
    ON COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA  
    AND COMPROBANTE_CNT.TIPO = TIPO_COMPROBANTE.CODIGO 
  LEFT JOIN ORDENDECOMPRAPPTO 
    ON ORDENDECOMPRA.COMPANIA = ORDENDECOMPRAPPTO.COMPANIA 
    AND ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRAPPTO.CLASEORDEN 
    AND ORDENDECOMPRA.NUMERO = ORDENDECOMPRAPPTO.NUMERO 
WHERE ORDENDECOMPRA.COMPANIA =  s$compania$s 
AND EXTRACT(YEAR FROM ORDENDECOMPRA.FECHA)= s$ano$s
AND EXTRACT(MONTH FROM ORDENDECOMPRA.FECHA) s$tipoSemestre$s 
   s$generarContrato$s 
ORDER BY  ORDENDECOMPRA.NUMERO#')
WHERE INFORME='000437rptRelaciondeContratosSEM';
COMMIT;