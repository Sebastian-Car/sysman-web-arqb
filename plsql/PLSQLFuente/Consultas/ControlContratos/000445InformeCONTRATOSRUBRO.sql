WITH CONSULTA_CONTRATOS_RUBRO AS (SELECT
    DETALLE_COMPROBANTE_PPTAL.COMPANIA, 
    V_PLAN_PRESUPUESTAL.ID CODIGO_CUENTA, 
    COMPROBANTE_PPTAL.TIPOCONTRATO,
    COMPROBANTE_PPTAL.NUMEROCONTRATO,
    ORDENDECOMPRA.VALORTOTAL,
    ORDENDECOMPRA.NOMBRECONTRATISTA,
    ORDENDECOMPRA.CEDULACONTRATISTA,
    ORDENDECOMPRA.INTERVENTOR,
    ORDENDECOMPRA.ORDENADOR,
    ORDENDECOMPRA.FECHA,
    ORDENDECOMPRA.FECHAFINALIZACION,
    ORDENDECOMPRA.CLASEORDEN,
    ORDENDECOMPRA.NUMERO,
    ORDENDECOMPRA.TERCERO,
    ORDENADOR.NOMBRE ORDENADOR_NOMBRE,
    AUXILIAR.NOMBRE
FROM (DETALLE_COMPROBANTE_PPTAL INNER JOIN COMPROBANTE_PPTAL 
    ON (DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO) 
        AND (DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = COMPROBANTE_PPTAL.TIPO) 
        AND (DETALLE_COMPROBANTE_PPTAL.ANO = COMPROBANTE_PPTAL.ANO) 
        AND (DETALLE_COMPROBANTE_PPTAL.COMPANIA = COMPROBANTE_PPTAL.COMPANIA)
    INNER JOIN V_PLAN_PRESUPUESTAL
        ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = V_PLAN_PRESUPUESTAL.COMPANIA
            AND DETALLE_COMPROBANTE_PPTAL.CUENTA = V_PLAN_PRESUPUESTAL.ID
            AND DETALLE_COMPROBANTE_PPTAL.ANO = V_PLAN_PRESUPUESTAL.ANO) INNER JOIN 
            (ORDENDECOMPRA LEFT JOIN ORDENADOR 
                ON ORDENDECOMPRA.COMPANIA = ORDENADOR.COMPANIA
                    AND ORDENDECOMPRA.ORDENADOR = ORDENADOR.CEDULA 
            LEFT JOIN ORDENDECOMPRAPPTO
                ON ORDENDECOMPRA.COMPANIA = ORDENDECOMPRAPPTO.COMPANIA 
                    AND ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRAPPTO.CLASEORDEN
                    AND ORDENDECOMPRA.NUMERO = ORDENDECOMPRAPPTO.NUMERO
            LEFT JOIN AUXILIAR
                ON ORDENDECOMPRAPPTO.COMPANIA = AUXILIAR.COMPANIA
                    AND ORDENDECOMPRAPPTO.AUXILIAR = AUXILIAR.CODIGO
                    AND ORDENDECOMPRAPPTO.ANOPPTO = AUXILIAR.ANO)
        ON COMPROBANTE_PPTAL.COMPANIA = ORDENDECOMPRA.COMPANIA
            AND COMPROBANTE_PPTAL.TIPOCONTRATO = ORDENDECOMPRA.CLASEORDEN
            AND COMPROBANTE_PPTAL.NUMEROCONTRATO = ORDENDECOMPRA.NUMERO 
WHERE DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE ='RES' 
    AND COMPROBANTE_PPTAL.TIPOCONTRATO IS NOT NULL
GROUP BY 
    DETALLE_COMPROBANTE_PPTAL.COMPANIA,
    V_PLAN_PRESUPUESTAL.ID, 
    COMPROBANTE_PPTAL.TIPOCONTRATO, 
    COMPROBANTE_PPTAL.NUMEROCONTRATO, 
    ORDENDECOMPRA.VALORTOTAL, 
    ORDENDECOMPRA.NOMBRECONTRATISTA, 
    ORDENDECOMPRA.CEDULACONTRATISTA,
    ORDENDECOMPRA.INTERVENTOR, 
    ORDENDECOMPRA.ORDENADOR, 
    ORDENDECOMPRA.FECHA, ORDENDECOMPRA.FECHAFINALIZACION, ORDENDECOMPRA.CLASEORDEN, 
    ORDENDECOMPRA.NUMERO, ORDENDECOMPRA.TERCERO, ORDENADOR.NOMBRE, AUXILIAR.NOMBRE)
SELECT CONSULTA_CONTRATOS_RUBRO.COMPANIA, 
    CONSULTA_CONTRATOS_RUBRO.CODIGO_CUENTA, 
    CONSULTA_CONTRATOS_RUBRO.TIPOCONTRATO,
    CONSULTA_CONTRATOS_RUBRO.NUMEROCONTRATO,
    CONSULTA_CONTRATOS_RUBRO.VALORTOTAL,
    CONSULTA_CONTRATOS_RUBRO.NOMBRECONTRATISTA,
    CONSULTA_CONTRATOS_RUBRO.CEDULACONTRATISTA,
    CONSULTA_CONTRATOS_RUBRO.INTERVENTOR,
    CONSULTA_CONTRATOS_RUBRO.ORDENADOR,
    CONSULTA_CONTRATOS_RUBRO.FECHA,
    CONSULTA_CONTRATOS_RUBRO.FECHAFINALIZACION,
    CONSULTA_CONTRATOS_RUBRO.CLASEORDEN,
    CONSULTA_CONTRATOS_RUBRO.NUMERO,
    CONSULTA_CONTRATOS_RUBRO.TERCERO,
    CONSULTA_CONTRATOS_RUBRO.ORDENADOR_NOMBRE,
    CONSULTA_CONTRATOS_RUBRO.NOMBRE,
    ORDENDECOMPRA.OBJETOCONTRATO
FROM ORDENDECOMPRA INNER JOIN CONSULTA_CONTRATOS_RUBRO
    ON ORDENDECOMPRA.COMPANIA = CONSULTA_CONTRATOS_RUBRO.COMPANIA
        AND ORDENDECOMPRA.CLASEORDEN = CONSULTA_CONTRATOS_RUBRO.CLASEORDEN
        AND ORDENDECOMPRA.NUMERO = CONSULTA_CONTRATOS_RUBRO.NUMERO
WHERE ORDENDECOMPRA.COMPANIA = s$compania$s
        AND CONSULTA_CONTRATOS_RUBRO.CODIGO_CUENTA 
        BETWEEN 's$rubroInicial$s'
            AND 's$rubroFinal$s'
    AND CONSULTA_CONTRATOS_RUBRO.FECHA 
        BETWEEN s$fechaInicial$s
            AND s$fechaFinal$s
    AND CONSULTA_CONTRATOS_RUBRO.TIPOCONTRATO 
        BETWEEN 's$tipoContratoInicial$s'
            AND 's$tipoContratoFinal$s'
ORDER BY CONSULTA_CONTRATOS_RUBRO.TIPOCONTRATO
