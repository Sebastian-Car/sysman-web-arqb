WITH INICIO AS (SELECT 
ROW_NUMBER() OVER (ORDER BY OrdenDeCompra.COMPANIA,OrdenDeCompra.CLASEORDEN,OrdenDeCompra.NUMERO) ORDE, 
OrdenDeCompra.COMPANIA,  
OrdenDeCompra.CLASEORDEN,  
OrdenDeCompra.NUMERO ORDENDECOMPRA_NUMERO, 
TO_CHAR(OrdenDeCompra.FECHA,'DD/MM/YYYY') ORDENDECOMPRA_FECHA,  
OrdenDeCompra.FECHAFINALIZACION,  
OrdenDeCompra.OBJETOCONTRATO, 
NovedadContrato.TIPOT,  
ClaseTransaccionC.Nombre NOMBRETIPO, 
NovedadContrato.NOVEDAD,  
NovedadContrato.NUMERO NOVEDADCONTRATO_NUMERO,  
TipoOrdenDeCompra.NOMBRE, 
Tercero.NOMBRE TERCERO_NOMBRE, 
NovedadContrato.FECHA NOVEDADCONTRATO_FECHA, 
OrdenDeCompra.VALORTOTAL ORDENDECOMPRA_VALORTOTAL, 
ORDENDECOMPRA.VALORFINAL, 
OrdenDeCompra.valorpagos VALORPAGOSF, 
NovedadContrato.VALORTOTAL NOVEDADCONTRATO_VALORTOTAL, 
TIPOORDENDECOMPRA.Clase, 
ORDENDECOMPRA.CLASENOVEDAD 
FROM OrdenDeCompra  
INNER JOIN Tercero ON  
 OrdenDeCompra.COMPANIA = Tercero.COMPANIA AND 
 OrdenDeCompra.TERCERO = Tercero.NIT AND 
 OrdenDeCompra.SUCURSAL = Tercero.SUCURSAL  
LEFT JOIN NovedadContrato ON  
OrdenDeCompra.COMPANIA = NovedadContrato.COMPANIA AND  
OrdenDeCompra.CLASEORDEN = NovedadContrato.CLASEORDEN AND  
OrdenDeCompra.NUMERO = NovedadContrato.ORDENDECOMPRA 
LEFT JOIN ClaseTransaccionC  
  ON NovedadContrato.CLASET = ClaseTransaccionC.CLASET AND  
     NovedadContrato.TIPOT = ClaseTransaccionC.TIPOT  
LEFT JOIN  
TipoOrdenDeCompra ON  
OrdenDeCompra.COMPANIA = TipoOrdenDeCompra.COMPANIA  AND 
OrdenDeCompra.CLASEORDEN = TipoOrdenDeCompra.CODIGO  WHERE  
OrdenDeCompra.COMPANIA=s$compania$s  
AND OrdenDeCompra.CLASEORDEN='s$claseOrden$s'  
AND OrdenDeCompra.NUMERO =s$numero$s  
AND nvl(TipoOrdenDeCompra.CLASE,'')<>'M'  
ORDER BY OrdenDeCompra.COMPANIA, 
OrdenDeCompra.CLASEORDEN, 
OrdenDeCompra.NUMERO 
), 
ACUMACP AS (  
    SELECT INICIO.COMPANIA, INICIO.ORDE, 
          SUM(INICIO.NOVEDADCONTRATO_VALORTOTAL) OVER (ORDER BY  INICIO.COMPANIA,INICIO.CLASEORDEN,INICIO.ORDENDECOMPRA_NUMERO ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) ACUM 
    FROM INICIO 
    WHERE INICIO.TIPOT='RES' 
     AND (INICIO.CLASE <>'M' OR INICIO.CLASE IS NULL) 
), 
ACUMA AS ( 
    SELECT INICIO.COMPANIA, INICIO.ORDE, 
          MIN(INICIO.ORDENDECOMPRA_VALORTOTAL) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) PRIMER      
    FROM INICIO 
    WHERE INICIO.TIPOT='RES' 
     AND (INICIO.CLASE <>'M' OR INICIO.CLASE IS NULL) 
) 
SELECT INICIO.COMPANIA, INICIO.ORDE, INICIO.TIPOT,INICIO.NOMBRETIPO, INICIO.ORDENDECOMPRA_VALORTOTAL,  
        INICIO.NOVEDADCONTRATO_VALORTOTAL,  
        ACUMACP.ACUM, 
        ACUMA.PRIMER-ACUMACP.ACUM SALDO, 
        INICIO.ORDENDECOMPRA_NUMERO, 
        INICIO.TERCERO_NOMBRE, 
        INICIO.ORDENDECOMPRA_FECHA, 
        INICIO.OBJETOCONTRATO, 
        INICIO.VALORFINAL, 
        INICIO.VALORPAGOSF, 
        INICIO.NOVEDADCONTRATO_NUMERO, 
        INICIO.NOVEDADCONTRATO_FECHA 
        
FROM INICIO, ACUMACP, ACUMA 
WHERE INICIO.COMPANIA = ACUMACP.COMPANIA  
  AND INICIO.ORDE = ACUMACP.ORDE  
  AND INICIO.TIPOT='RES' 
  AND (INICIO.CLASE <>'M' OR INICIO.CLASE IS NULL) 
   
UNION  
SELECT INICIO.COMPANIA, INICIO.ORDE, INICIO.TIPOT, INICIO.NOMBRETIPO,INICIO.ORDENDECOMPRA_VALORTOTAL,  
        INICIO.NOVEDADCONTRATO_VALORTOTAL,  
        0, 
        0 SALDO, 
        INICIO.ORDENDECOMPRA_NUMERO, 
        INICIO.TERCERO_NOMBRE, 
        INICIO.ORDENDECOMPRA_FECHA, 
        INICIO.OBJETOCONTRATO, 
        INICIO.VALORFINAL, 
        INICIO.VALORPAGOSF, 
        INICIO.NOVEDADCONTRATO_NUMERO, 
        INICIO.NOVEDADCONTRATO_FECHA 
FROM INICIO 
WHERE ((TIPOT<>'RES' OR TIPOT IS NULL) AND (INICIO.CLASE <>'M' OR INICIO.CLASE IS NULL)) 
OR( INICIO.CLASE ='M' AND INICIO.CLASENOVEDAD='D')