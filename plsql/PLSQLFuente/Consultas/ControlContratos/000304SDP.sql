SELECT  SOLICITUDDISPONIBILIDAD.NUMERO, 
        SOLICITUDDISPONIBILIDAD.VALOR, 
        DEPENDENCIA.NOMBRE  DEPENDENCIA, 
        SOLICITUDDISPONIBILIDAD.OBJETO, 
        LISTAGG( D_SOLICITUDDISPONIBILIDAD.RUBRO, ', ') WITHIN GROUP (ORDER BY D_SOLICITUDDISPONIBILIDAD.RUBRO) RUBRO
FROM    SOLICITUDDISPONIBILIDAD 
  INNER JOIN DEPENDENCIA 
    ON SOLICITUDDISPONIBILIDAD.DEPENDENCIA = DEPENDENCIA.CODIGO
    AND SOLICITUDDISPONIBILIDAD.COMPANIA = DEPENDENCIA.COMPANIA
  LEFT JOIN D_SOLICITUDDISPONIBILIDAD
    ON SOLICITUDDISPONIBILIDAD.COMPANIA = D_SOLICITUDDISPONIBILIDAD.COMPANIA
    AND SOLICITUDDISPONIBILIDAD.NUMERO = D_SOLICITUDDISPONIBILIDAD.NUMERO
WHERE   SOLICITUDDISPONIBILIDAD.COMPANIA =  s$compania$s 
AND     SOLICITUDDISPONIBILIDAD.NUMERO = s$numSolicitud$s
GROUP BY SOLICITUDDISPONIBILIDAD.NUMERO, 
        SOLICITUDDISPONIBILIDAD.VALOR, 
        DEPENDENCIA.NOMBRE  , 
        SOLICITUDDISPONIBILIDAD.OBJETO
