SELECT DISTINCT
  ORDENDECOMPRA.NUMERO, 
  ORDENDECOMPRA.COMPANIA,
  ORDENDECOMPRA.CLASEORDEN,
  TERCERO.NOMBRE, 
  TERCERO.DIRECCION, 
  TERCERO.TELEFONOS, 
  ORDENDECOMPRA.OBJETOCONTRATO, 
  ORDENDECOMPRA.VALORTOTAL VALORCONTRATO, 
  TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') FECHA,
  TO_CHAR(ORDENDECOMPRA.FECHAFINALIZACION,'DD/MM/YYYY') FECHAFINALIZACION, 
  V_ORDENDECOMPRA_DIS.COMPROBANTES DISPONIBILIDAD, 
  V_ORDENDECOMPRA_RES.COMPROBANTES RESERVA, 
  (CASE WHEN NATURALEZA ='N' THEN 'NATURAL' ELSE
  (CASE WHEN NATURALEZA ='J' THEN 'JURIDICA' ELSE '' END) END) FIGURAJURICA, 
  TO_CHAR(ORDENDECOMPRA.FECHA,'YYYY') VIGENCIA, 
  TERCERO.DIRECCIONEMAIL, 
  TERCERO.NIT,
  PCK_CONTRATOS.FC_NONOVEDAD(ORDENDECOMPRA.CLASEORDEN,ORDENDECOMPRA.NUMERO,1,'E',ORDENDECOMPRA.COMPANIA) ACTALIQUIDACIONFINAL,
  PCK_CONTRATOS.FC_NONOVEDAD(ORDENDECOMPRA.CLASEORDEN,ORDENDECOMPRA.NUMERO,2,'E',ORDENDECOMPRA.COMPANIA) FECHANOVEDAD,
  CASE WHEN CODCONTTOLIMA ='C1' THEN 'X' ELSE ' ' END SERVICIOS,
  CASE WHEN CODCONTTOLIMA ='C9' THEN 'X' ELSE ' ' END CONSULTORIA,
  CASE WHEN CODCONTTOLIMA ='C10' THEN 'X' ELSE ' ' END OTRO,
  CASE WHEN CODCONTTOLIMA ='C2' THEN 'X' ELSE ' ' END SUMINISTRO,
  CASE WHEN CODCONTTOLIMA ='C4' THEN 'X' ELSE ' ' END OBRA,
  CASE WHEN ORDENDECOMPRAPPTO.FUENTE_RECURSO = '1' THEN 'X' ELSE ' ' END PROPIOS,
  CASE WHEN ORDENDECOMPRAPPTO.FUENTE_RECURSO = '3' THEN 'X' ELSE ' ' END CONVENIO,
  CASE WHEN ORDENDECOMPRAPPTO.FUENTE_RECURSO = '2' THEN 'X' ELSE ' ' END OTROS     
FROM ORDENDECOMPRA 
       INNER JOIN TIPOORDENDECOMPRA 
              ON  ORDENDECOMPRA.COMPANIA  = TIPOORDENDECOMPRA.COMPANIA 
             AND ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO 
       INNER JOIN TERCERO 
              ON  ORDENDECOMPRA.COMPANIA  = TERCERO.COMPANIA 
             AND ORDENDECOMPRA.TERCERO    = TERCERO.NIT 
             AND ORDENDECOMPRA.SUCURSAL   = TERCERO.SUCURSAL 
       LEFT JOIN NOVEDADCONTRATO 
              ON  ORDENDECOMPRA.COMPANIA   = NOVEDADCONTRATO.COMPANIA
             AND ORDENDECOMPRA.CLASEORDEN  = NOVEDADCONTRATO.CLASEORDEN 
             AND ORDENDECOMPRA.NUMERO      = NOVEDADCONTRATO.ORDENDECOMPRA 
       LEFT JOIN ORDENDECOMPRAPPTO
              ON ORDENDECOMPRA.COMPANIA    = ORDENDECOMPRAPPTO.COMPANIA
             AND ORDENDECOMPRA.CLASEORDEN  = ORDENDECOMPRAPPTO.CLASEORDEN
             AND ORDENDECOMPRA.NUMERO      = ORDENDECOMPRAPPTO.NUMERO
       LEFT JOIN V_ORDENDECOMPRA_RES
              ON  ORDENDECOMPRA.COMPANIA   = V_ORDENDECOMPRA_RES.COMPANIA
             AND ORDENDECOMPRA.CLASEORDEN  = V_ORDENDECOMPRA_RES.CLASEORDEN
             AND ORDENDECOMPRA.NUMERO      = V_ORDENDECOMPRA_RES.NUMERO
       LEFT JOIN V_ORDENDECOMPRA_DIS
              ON  ORDENDECOMPRA.COMPANIA   = V_ORDENDECOMPRA_DIS.COMPANIA
             AND ORDENDECOMPRA.CLASEORDEN  = V_ORDENDECOMPRA_DIS.CLASEORDEN
             AND ORDENDECOMPRA.NUMERO      = V_ORDENDECOMPRA_DIS.NUMERO              
WHERE ORDENDECOMPRA.COMPANIA   = s$compania$s
  AND ORDENDECOMPRA.CLASEORDEN BETWEEN s$tipoContratoInicial$s AND s$tipoContratoFinal$s
  AND ORDENDECOMPRA.TERCERO    BETWEEN s$terceroInicial$s  AND s$terceroFinal$s
  AND ORDENDECOMPRA.FECHA      BETWEEN s$fechaInicial$s AND s$fechaFinal$s
 ORDER BY ORDENDECOMPRA.NUMERO, 
          TERCERO.DIRECCION          
