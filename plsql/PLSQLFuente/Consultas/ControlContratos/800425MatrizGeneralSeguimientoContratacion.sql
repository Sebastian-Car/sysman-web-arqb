SELECT DISTINCT TO_CHAR(ORDENDECOMPRA.NUMERO) NUMERO_CONTRATO,
  TO_CHAR(ORDENDECOMPRA.FECHADILIGENCIAMIENTO, 'DD/MM/YYYY') FECHA_CONTRATO,
  ORDENDECOMPRA.LUGARDEENTREGA LUGAR_EJECUCION_CONTRATO,
  ORDENDECOMPRA.NOPROCESO NO_PROCESO,
  TO_CHAR(ORDENDECOMPRA.FECHAINICIO, 'DD/MM/YYYY') FECHA_INICIO,
  TO_CHAR(NOVEDADCONTRATO.FECHAFINAL, 'DD/MM/YYYY') FECHA_LIQUIDACION,
  TO_CHAR(ORDENDECOMPRA.FECHAFINALIZACION, 'DD/MM/YYYY') FECHA_TERMINACION,
  CASE
    WHEN ORDENDECOMPRA.ESTADO = 'V'
    THEN 'Vigente'
    ELSE
      CASE
        WHEN ORDENDECOMPRA.ESTADO = 'A'
        THEN 'Anulada'
        ELSE
          CASE
            WHEN ORDENDECOMPRA.ESTADO = 'T'
            THEN 'Terminada'
            ELSE
              CASE
                WHEN ORDENDECOMPRA.ESTADO = 'L'
                THEN 'Liquidada'
                ELSE
                  CASE
                    WHEN ORDENDECOMPRA.ESTADO = 'S'
                    THEN 'Suspendido'
                    ELSE
                      CASE
                        WHEN ORDENDECOMPRA.ESTADO = 'M'
                        THEN 'Terminado por mutuo acuerdo'
                        ELSE
                          CASE
                            WHEN ORDENDECOMPRA.ESTADO = 'P'
                            THEN 'En proceso de sanción o proceso judicial'
                            ELSE ''
                          END
                      END
                  END
              END
          END
      END
  END ESTADO,
  ' ' ORIGEN_PRESUPUESTO,
  CASE
    WHEN ORDENDECOMPRA.DESTINOFIN = 'I'
    THEN 'Inversión'
    ELSE
      CASE
        WHEN ORDENDECOMPRA.DESTINOFIN = 'F'
        THEN 'Funcionamiento'
        ELSE ''
      END
  END TIPO_GASTO,
  SECTORES.DESCRIPCION NOMBRE_SECTOR,
  V_ORDENDECOMPRA_RUBRO.RUBRO RUBRO_PPTAL,
  V_ORDENDECOMPRA_RUBRO.NOMBRE NOMBRE_RUBRO_PPTAL,
  V_ORDENDECOMPRA_DIS.COMPROBANTES CDP,
  V_ORDENDECOMPRA_DIS.FECHACDP,
  V_ORDENDECOMPRA_DIS.TOTAL_DIS VALOR_CDP,
  V_ORDENDECOMPRA_RES.COMPROBANTES RP,
  V_ORDENDECOMPRA_RES.FECHACDP FECHA,
  V_ORDENDECOMPRA_RES.TOTAL_RES VR_RP,
  ORDENDECOMPRA.VALORFINAL VALOR_INICIAL,
  NOVEDADCONTRATOPRO.VALORTOTAL PRORROGAS,
  NOVEDADCONTRATOPRO.FECHAINICIAL FECHA_PRORROGAS,
  NOVEDADCONTRATOADI.VALORTOTAL ADICIONES,
  NOVEDADCONTRATOADI.FECHAINICIAL FECHA_ADICION,
  ORDENDECOMPRA.VALORTOTAL - ORDENDECOMPRA.VALORFINAL VALOR_ADICIONAL,
  V_ORDENDECOMPRA_DIS1.COMPROBANTES NO_CDP,
  V_ORDENDECOMPRA_RES1.NUMERO NO_RP,
  CASE
    WHEN FUENTE_RECURSOS.TIPO = 'P'
    THEN SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO)
    ELSE 0
  END RECURSOS_PROPIOS,
  CASE
    WHEN FUENTE_RECURSOS.TIPO = 'REL'
    THEN SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO)
    ELSE 0
  END REGALIAS,
  CASE
    WHEN FUENTE_RECURSOS.TIPO = 'SGP'
    THEN SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO)
    ELSE 0
  END SGP,
  CASE
    WHEN FUENTE_RECURSOS.TIPO NOT IN ('P','REL','SGP')
    THEN SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO)
    ELSE 0
  END OTROS_APORTES,
  ORDENDECOMPRA.PORC_ANTICIPO,
  ORDENDECOMPRA.OBJETOCONTRATO,
  ORDENDECOMPRA.NOMBRECONTRATISTA,
  ORDENDECOMPRA.TERCERO,
  CASE
    WHEN TERCERO.NATURALEZA = 'J'
    THEN 'JURIDICA'
    ELSE
      CASE
        WHEN TERCERO.NATURALEZA = 'N'
        THEN 'NATURAL'
        ELSE ' '
      END
  END PERSONA_NJ,
  TERCERO.DIRECCIONEMAIL CORREO_ELECTRONICO,
  TERCERO.CARGO PERFIL_CONTRATISTA,
  TIPOORDENDECOMPRA.NOMBRE CLASE_CONTRATO,
  TIPOADJUDICACION.EQUIVALENTESIA MODALIDAD_SELECCION,
  ORDENDECOMPRA.DURACION PLAZO_EJECUCION,
  SUPERVISOR.NIT NIT_SUPERVISOR,
  SUPERVISOR.NOMBRE NOMBRE_SUPERVISOR,
  ORDENDECOMPRA.VALORTOTAL VALOR_TOTAL_CONTRATO,
  '0' NO_CERTIFICADO,
  '0' VALOR_AUTORIZADO,
  ' ' OBSERVACIONES
FROM ORDENDECOMPRA
LEFT JOIN NOVEDADCONTRATO
ON ORDENDECOMPRA.COMPANIA    = NOVEDADCONTRATO.COMPANIA
AND ORDENDECOMPRA.CLASEORDEN = NOVEDADCONTRATO.CLASEORDEN
AND ORDENDECOMPRA.NUMERO     = NOVEDADCONTRATO.ORDENDECOMPRA
AND NOVEDADCONTRATO.TIPOT    = 'ACL'
LEFT JOIN NOVEDADCONTRATO NOVEDADCONTRATOPRO
ON ORDENDECOMPRA.COMPANIA    = NOVEDADCONTRATOPRO.COMPANIA
AND ORDENDECOMPRA.CLASEORDEN = NOVEDADCONTRATOPRO.CLASEORDEN
AND ORDENDECOMPRA.NUMERO     = NOVEDADCONTRATOPRO.ORDENDECOMPRA
AND NOVEDADCONTRATOPRO.TIPOT = 'PRO'
LEFT JOIN NOVEDADCONTRATO NOVEDADCONTRATOADI
ON ORDENDECOMPRA.COMPANIA    = NOVEDADCONTRATOADI.COMPANIA
AND ORDENDECOMPRA.CLASEORDEN = NOVEDADCONTRATOADI.CLASEORDEN
AND ORDENDECOMPRA.NUMERO     = NOVEDADCONTRATOADI.ORDENDECOMPRA
AND NOVEDADCONTRATOADI.TIPOT = 'ADI'
LEFT JOIN SECTORES
ON ORDENDECOMPRA.COMPANIA = SECTORES.COMPANIA
AND ORDENDECOMPRA.SECTOR  = SECTORES.CODIGO
LEFT JOIN V_ORDENDECOMPRA_RUBRO
ON ORDENDECOMPRA.COMPANIA    = V_ORDENDECOMPRA_RUBRO.COMPANIA
AND ORDENDECOMPRA.CLASEORDEN = V_ORDENDECOMPRA_RUBRO.CLASEORDEN
AND ORDENDECOMPRA.NUMERO     = V_ORDENDECOMPRA_RUBRO.NUMERO
LEFT JOIN COMPROBANTE_PPTAL
ON ORDENDECOMPRA.COMPANIA    = COMPROBANTE_PPTAL.COMPANIA
AND ORDENDECOMPRA.CLASEORDEN = COMPROBANTE_PPTAL.TIPOCONTRATO
AND ORDENDECOMPRA.NUMERO     = COMPROBANTE_PPTAL.NUMEROCONTRATO
INNER JOIN DETALLE_COMPROBANTE_PPTAL
ON COMPROBANTE_PPTAL.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA
AND COMPROBANTE_PPTAL.ANO     = DETALLE_COMPROBANTE_PPTAL.ANO
AND COMPROBANTE_PPTAL.TIPO    = DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE
AND COMPROBANTE_PPTAL.NUMERO  = DETALLE_COMPROBANTE_PPTAL.COMPROBANTE
INNER JOIN PLAN_PRESUPUESTAL
ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
AND DETALLE_COMPROBANTE_PPTAL.ANO     = PLAN_PRESUPUESTAL.ANO
AND DETALLE_COMPROBANTE_PPTAL.CUENTA  = PLAN_PRESUPUESTAL.CODIGO
LEFT JOIN V_ORDENDECOMPRA_DIS
ON ORDENDECOMPRA.COMPANIA    = V_ORDENDECOMPRA_DIS.COMPANIA
AND ORDENDECOMPRA.CLASEORDEN = V_ORDENDECOMPRA_DIS.CLASEORDEN
AND ORDENDECOMPRA.NUMERO     = V_ORDENDECOMPRA_DIS.NUMERO
LEFT JOIN V_ORDENDECOMPRA_RES
ON ORDENDECOMPRA.COMPANIA    = V_ORDENDECOMPRA_RES.COMPANIA
AND ORDENDECOMPRA.CLASEORDEN = V_ORDENDECOMPRA_RES.CLASEORDEN
AND ORDENDECOMPRA.NUMERO     = V_ORDENDECOMPRA_RES.NUMERO
LEFT JOIN V_ORDENDECOMPRA_DIS V_ORDENDECOMPRA_DIS1
ON ORDENDECOMPRA.COMPANIA        = V_ORDENDECOMPRA_DIS1.COMPANIA
AND ORDENDECOMPRA.TIPOAFECTADO   = V_ORDENDECOMPRA_DIS1.TIPO
AND ORDENDECOMPRA.NUMEROAFECTADO = V_ORDENDECOMPRA_DIS1.NUMERO
LEFT JOIN V_ORDENDECOMPRA_RES V_ORDENDECOMPRA_RES1
ON ORDENDECOMPRA.COMPANIA        = V_ORDENDECOMPRA_RES1.COMPANIA
AND ORDENDECOMPRA.TIPOAFECTADO   = V_ORDENDECOMPRA_RES1.TIPO
AND ORDENDECOMPRA.NUMEROAFECTADO = V_ORDENDECOMPRA_RES1.NUMERO
INNER JOIN FUENTE_RECURSOS
ON DETALLE_COMPROBANTE_PPTAL.COMPANIA        = FUENTE_RECURSOS.COMPANIA
AND DETALLE_COMPROBANTE_PPTAL.ANO            = FUENTE_RECURSOS.ANO
AND DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO = FUENTE_RECURSOS.CODIGO
INNER JOIN TERCERO
ON ORDENDECOMPRA.COMPANIA  = TERCERO.COMPANIA
AND ORDENDECOMPRA.TERCERO  = TERCERO.NIT
AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL
INNER JOIN TIPOORDENDECOMPRA
ON ORDENDECOMPRA.COMPANIA    = TIPOORDENDECOMPRA.COMPANIA
AND ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO
LEFT JOIN TIPOADJUDICACION
ON ORDENDECOMPRA.COMPANIA          = TIPOADJUDICACION.COMPANIA
AND ORDENDECOMPRA.TIPOADJUDICACION = TIPOADJUDICACION.CODIGO
LEFT JOIN SUPERVISORES
ON ORDENDECOMPRA.COMPANIA    = SUPERVISORES.COMPANIA
AND ORDENDECOMPRA.CLASEORDEN = SUPERVISORES.CLASEORDEN
AND ORDENDECOMPRA.NUMERO     = SUPERVISORES.NUMEROCONTRATO
LEFT JOIN TERCERO SUPERVISOR
ON SUPERVISORES.COMPANIA  = SUPERVISOR.COMPANIA
AND SUPERVISORES.CEDULA   = SUPERVISOR.NIT
AND SUPERVISORES.SUCURSAL = SUPERVISOR.SUCURSAL
WHERE (ORDENDECOMPRA.COMPANIA = s$compania$s
AND TIPOORDENDECOMPRA.CLASE  = 'C')
AND ORDENDECOMPRA.FECHA BETWEEN 's$fechaInicial$s' AND 's$fechaFinal$s'
GROUP BY ORDENDECOMPRA.NUMERO,
  TO_CHAR(ORDENDECOMPRA.FECHADILIGENCIAMIENTO, 'DD/MM/YYYY'),
  ORDENDECOMPRA.LUGARDEENTREGA,
  ORDENDECOMPRA.NOPROCESO,
  TO_CHAR(ORDENDECOMPRA.FECHAINICIO, 'DD/MM/YYYY'),
  TO_CHAR(NOVEDADCONTRATO.FECHAFINAL, 'DD/MM/YYYY'),
  TO_CHAR(ORDENDECOMPRA.FECHAFINALIZACION, 'DD/MM/YYYY'),
  CASE
    WHEN ORDENDECOMPRA.ESTADO = 'V'
    THEN 'Vigente'
    ELSE
      CASE
        WHEN ORDENDECOMPRA.ESTADO = 'A'
        THEN 'Anulada'
        ELSE
          CASE
            WHEN ORDENDECOMPRA.ESTADO = 'T'
            THEN 'Terminada'
            ELSE
              CASE
                WHEN ORDENDECOMPRA.ESTADO = 'L'
                THEN 'Liquidada'
                ELSE
                  CASE
                    WHEN ORDENDECOMPRA.ESTADO = 'S'
                    THEN 'Suspendido'
                    ELSE
                      CASE
                        WHEN ORDENDECOMPRA.ESTADO = 'M'
                        THEN 'Terminado por mutuo acuerdo'
                        ELSE
                          CASE
                            WHEN ORDENDECOMPRA.ESTADO = 'P'
                            THEN 'En proceso de sanción o proceso judicial'
                            ELSE ''
                          END
                      END
                  END
              END
          END
      END
  END,
  CASE
    WHEN ORDENDECOMPRA.DESTINOFIN = 'I'
    THEN 'Inversión'
    ELSE
      CASE
        WHEN ORDENDECOMPRA.DESTINOFIN = 'F'
        THEN 'Funcionamiento'
        ELSE ''
      END
  END,
  SECTORES.DESCRIPCION,
  V_ORDENDECOMPRA_RUBRO.RUBRO,
  V_ORDENDECOMPRA_RUBRO.NOMBRE,
  V_ORDENDECOMPRA_DIS.COMPROBANTES,
  V_ORDENDECOMPRA_DIS.FECHACDP,
  V_ORDENDECOMPRA_DIS.TOTAL_DIS,
  V_ORDENDECOMPRA_RES.COMPROBANTES,
  V_ORDENDECOMPRA_RES.FECHACDP,
  V_ORDENDECOMPRA_RES.TOTAL_RES,
  ORDENDECOMPRA.VALORFINAL,
  NOVEDADCONTRATOPRO.VALORTOTAL,
  NOVEDADCONTRATOPRO.FECHAINICIAL,
  NOVEDADCONTRATOADI.VALORTOTAL,
  NOVEDADCONTRATOADI.FECHAINICIAL,
  ORDENDECOMPRA.VALORTOTAL,
  V_ORDENDECOMPRA_DIS1.COMPROBANTES,
  V_ORDENDECOMPRA_RES1.NUMERO,
  FUENTE_RECURSOS.TIPO,
  ORDENDECOMPRA.PORC_ANTICIPO,
  ORDENDECOMPRA.OBJETOCONTRATO,
  ORDENDECOMPRA.NOMBRECONTRATISTA,
  ORDENDECOMPRA.TERCERO,
  CASE
    WHEN TERCERO.NATURALEZA = 'J'
    THEN 'JURIDICA'
    ELSE
      CASE
        WHEN TERCERO.NATURALEZA = 'N'
        THEN 'NATURAL'
        ELSE ' '
      END
  END,
  TERCERO.DIRECCIONEMAIL,
  TERCERO.CARGO,
  TIPOORDENDECOMPRA.NOMBRE,
  TIPOADJUDICACION.EQUIVALENTESIA,
  ORDENDECOMPRA.DURACION,
  SUPERVISOR.NIT,
  SUPERVISOR.NOMBRE,
  ORDENDECOMPRA.VALORTOTAL  