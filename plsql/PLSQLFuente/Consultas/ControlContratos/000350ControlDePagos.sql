UPDATE CONSULTAS SET CONSULTA=TO_CLOB(q'#WITH C_CONTROLDEPAGOS AS 
  (SELECT  COMPROBANTE_CNT.COMPANIA,
      TIPO_COMPROBANTE.NOMBRE, 
      COMPROBANTE_CNT.ANO,
      COMPROBANTE_CNT.TIPO,
      COMPROBANTE_CNT.NUMERO,
      COMPROBANTE_CNT.TIPOCONTRATO,
      COMPROBANTE_CNT.FECHA, 
      COMPROBANTE_CNT.NUMEROCONTRATO, 
      COMPROBANTE_CNT.VLR_DOCUMENTO
  FROM COMPROBANTE_CNT INNER JOIN TIPO_COMPROBANTE 
      ON COMPROBANTE_CNT.COMPANIA = TIPO_COMPROBANTE.COMPANIA
          AND COMPROBANTE_CNT.TIPO = TIPO_COMPROBANTE.CODIGO) 
SELECT  ORDENDECOMPRA.COMPANIA, 
       ORDENDECOMPRA.CLASEORDEN, 
        ORDENDECOMPRA.NUMERO, 
        NVL(TERCERO.NOMBRE, ' ') NOMBRE, 
        NVL(ORDENDECOMPRA.OBJETOCONTRATO, ' ') OBJETOCONTRATO, 
        NVL(ORDENDECOMPRA.VALORTOTAL, 0) VALORTOTAL, 
        NVL(ORDENDECOMPRA.INTERVENTOR, ' ') INTERVENTOR, 
        ORDENDECOMPRA.FECHA FECHAORDEN, 
        ORDENDECOMPRA.FECHAFINALIZACION, 
        TIPOORDENDECOMPRA.NOMBRE NOMBRETIPOORDEN, 
        C_CONTROLDEPAGOS.NOMBRE NOMBRECOMPROBANTE,
        C_CONTROLDEPAGOS.TIPO TIPOCONTROL,
        TO_CHAR(C_CONTROLDEPAGOS.NUMERO) NUMEROCONTROL, 
        C_CONTROLDEPAGOS.VLR_DOCUMENTO VALORDOCCONTROL, 
        C_CONTROLDEPAGOS.FECHA FECHACONTROL
FROM ORDENDECOMPRA INNER JOIN TIPOORDENDECOMPRA 
    ON ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA 
        AND ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO
LEFT JOIN C_CONTROLDEPAGOS 
    ON ORDENDECOMPRA.COMPANIA = C_CONTROLDEPAGOS.COMPANIA  
        AND ORDENDECOMPRA.CLASEORDEN = C_CONTROLDEPAGOS.TIPOCONTRATO
        AND ORDENDECOMPRA.NUMERO = C_CONTROLDEPAGOS.NUMEROCONTRATO 
INNER JOIN TERCERO 
    ON ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA 
        AND ORDENDECOMPRA.TERCERO = TERCERO.NIT 
        AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL 
WHERE ORDENDECOMPRA.COMPANIA = s$compania$s 
    AND s$tablaFecha$s.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
    AND s$campoContrato$s BETWEEN 's$tipoContratoInicial$s' AND 's$tipoContratoFinal$s'
    AND ORDENDECOMPRA.ESTADO<>'A' AND NVL(TIPOORDENDECOMPRA.CLASE,'') NOT IN 'M'
ORDER BY ORDENDECOMPRA.COMPANIA, ORDENDECOMPRA.CLASEORDEN, ORDENDECOMPRA.NUMERO#')
WHERE INFORME='000350ControlDePagos';
COMMIT;