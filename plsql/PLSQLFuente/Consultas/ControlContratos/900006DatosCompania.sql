	   s$ano$s AÑO ,    
	   83 TIPOENTIDAD,    
	   SUBSTR(COMPANIA.NITCOMPANIA,1, CASE WHEN (INSTR(COMPANIA.NITCOMPANIA ,'-')) <>0 THEN INSTR(COMPANIA.NITCOMPANIA,'-') -1 ELSE LENGTH(COMPANIA.NITCOMPANIA)END) NIT,    
