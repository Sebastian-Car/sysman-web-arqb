SELECT  
       ORDENDECOMPRAPPTO.RUBRO || ' - ' || PLAN_PRESUPUESTAL.NOMBRE AS RUBROPPTAL, 
       TIPOORDENDECOMPRA.NOMBRE AS NOMBREORDEN, 
       TO_CHAR(ORDENDECOMPRA.FECHA,'DD/MM/YYYY') FECHA, 
       D_ORDENDECOMPRA.ORDENDECOMPRA, 
       TERCERO.NOMBRE AS NOMBREPROVEEDOR,  
       CASE WHEN D_ORDENDECOMPRA.ELEMENTO <> '9999999999999999' THEN INVENTARIO.NOMBRELARGO || ' ' || D_ORDENDECOMPRA.ESPECIFICACION ELSE D_ORDENDECOMPRA.ESPECIFICACION END AS ELEMENTOS, 
       DEPENDENCIA.NOMBRE AS NOMBREDEPENDENCIA,  
       D_ORDENDECOMPRA.VLRTOTAL 
FROM D_ORDENDECOMPRA  
  INNER JOIN ORDENDECOMPRA  
    ON D_ORDENDECOMPRA.COMPANIA = ORDENDECOMPRA.COMPANIA  
    AND D_ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRA.CLASEORDEN  
    AND D_ORDENDECOMPRA.ORDENDECOMPRA = ORDENDECOMPRA.NUMERO  
  INNER JOIN TERCERO  
    ON ORDENDECOMPRA.COMPANIA = TERCERO.COMPANIA  
    AND ORDENDECOMPRA.SUCURSAL = TERCERO.SUCURSAL  
    AND ORDENDECOMPRA.TERCERO = TERCERO.NIT  
  LEFT JOIN DEPENDENCIA  
    ON D_ORDENDECOMPRA.COMPANIA = DEPENDENCIA.COMPANIA  
    AND D_ORDENDECOMPRA.DEPENDENCIA = DEPENDENCIA.CODIGO  
  INNER JOIN TIPOORDENDECOMPRA 
    ON D_ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA  
    AND D_ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO  
  LEFT JOIN INVENTARIO  
    ON D_ORDENDECOMPRA.COMPANIA = INVENTARIO.COMPANIA  
    AND D_ORDENDECOMPRA.ELEMENTO = INVENTARIO.CODIGOELEMENTO  
  INNER JOIN ORDENDECOMPRAPPTO  
     ON ORDENDECOMPRA.COMPANIA = ORDENDECOMPRAPPTO.COMPANIA 
    AND ORDENDECOMPRA.CLASEORDEN = ORDENDECOMPRAPPTO.CLASEORDEN 
    AND ORDENDECOMPRA.NUMERO = ORDENDECOMPRAPPTO.NUMERO 
  LEFT JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL  
   ON  ORDENDECOMPRAPPTO.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA 
   AND ORDENDECOMPRAPPTO.ANOPPTO = PLAN_PRESUPUESTAL.ANO  
   AND ORDENDECOMPRAPPTO.RUBRO = PLAN_PRESUPUESTAL.ID     
  WHERE   D_ORDENDECOMPRA.COMPANIA =  s$compania$s   
    AND ORDENDECOMPRA.FECHA BETWEEN s$fechaInicial$s  AND s$fechaFinal$s  
    AND ORDENDECOMPRAPPTO.RUBRO BETWEEN  s$rubroInicial$s AND  s$rubroFinal$s 
ORDER BY D_ORDENDECOMPRA.COMPANIA, D_ORDENDECOMPRA.CLASEORDEN, D_ORDENDECOMPRA.ORDENDECOMPRA, D_ORDENDECOMPRA.CODIGO