SELECT  D_ORDENDESUMINISTRO.ORDENDESUMINISTRO,  
        TO_CHAR(ORDENDESUMINISTRO.FECHA,'DD/MM/YYYY') FECHA,  
        DEPENDENCIA_RESPONSABLE.CARGO,  
        DEPENDENCIA.NOMBRE  DEPENDENCIA,  
        ORDENDESUMINISTRO.DESCRIPCION,  
        D_ORDENDESUMINISTRO.CANTIDAD,  
        D_ORDENDESUMINISTRO.CANTIDADAPROBADA,  
        UNIDAD.NOMBRE  UNIDAD,  
        D_ORDENDESUMINISTRO.ELEMENTO,  
        CASE WHEN  
            D_ORDENDESUMINISTRO.ELEMENTO <> '9999999999999999' 
        THEN  
            INVENTARIO.NOMBRELARGO || D_ORDENDESUMINISTRO.ESPECIFICACION 
        ELSE 
            D_ORDENDESUMINISTRO.ESPECIFICACION 
        END  NOMBREELEMENTO,  
        D_ORDENDESUMINISTRO.CODIGOCUBS,  
        ORDENDESUMINISTRO.VALORESTIMADO,  
        ORDENDESUMINISTRO.OBSERVACIONES,  
        ORDENDESUMINISTRO.PERIODICIDAD,  
        ORDENDESUMINISTRO.NUMERO_ENTREGAS,  
        ORDENDESUMINISTRO.PLAZO || ' - ' || ORDENDESUMINISTRO.UNIDAD_TIEMPO  PLAZOT,  
        TERCERO.NOMBRE  RESPONSABLE,  
        ORDENDESUMINISTRO.TERCERO,  
        D_ORDENDESUMINISTRO.CODIGO 
FROM    D_ORDENDESUMINISTRO  
        LEFT JOIN ORDENDESUMINISTRO  
        ON  (D_ORDENDESUMINISTRO.COMPANIA = ORDENDESUMINISTRO.COMPANIA)  
        AND (D_ORDENDESUMINISTRO.ORDENDESUMINISTRO = ORDENDESUMINISTRO.NUMERO) 
        LEFT JOIN INVENTARIO  
        ON  (D_ORDENDESUMINISTRO.ELEMENTO = INVENTARIO.CODIGOELEMENTO)  
        AND (D_ORDENDESUMINISTRO.COMPANIA = INVENTARIO.COMPANIA) 
        LEFT JOIN DEPENDENCIA_RESPONSABLE  
        ON  (ORDENDESUMINISTRO.TERCERO = DEPENDENCIA_RESPONSABLE.RESPONSABLE)  
        AND (ORDENDESUMINISTRO.DEPENDENCIA = DEPENDENCIA_RESPONSABLE.DEPENDENCIA)  
        AND (ORDENDESUMINISTRO.COMPANIA = DEPENDENCIA_RESPONSABLE.COMPANIA) 
        AND (ORDENDESUMINISTRO.SUCURSAL = DEPENDENCIA_RESPONSABLE.SUCURSAL) 
        LEFT JOIN UNIDAD  
        ON INVENTARIO.UNIDAD = UNIDAD.UNIDAD  
        LEFT JOIN TERCERO 
        ON  ORDENDESUMINISTRO.COMPANIA = TERCERO.COMPANIA 
        AND ORDENDESUMINISTRO.TERCERO = TERCERO.NIT 
        AND ORDENDESUMINISTRO.SUCURSAL = TERCERO.SUCURSAL  
        LEFT JOIN DEPENDENCIA  
        ON  (ORDENDESUMINISTRO.DEPENDENCIA = DEPENDENCIA.CODIGO)  
        AND (ORDENDESUMINISTRO.COMPANIA = DEPENDENCIA.COMPANIA)
WHERE   D_ORDENDESUMINISTRO.COMPANIA =  s$compania$s   
AND     D_ORDENDESUMINISTRO.ORDENDESUMINISTRO = s$ordendesuministro$s  
ORDER BY  
    D_ORDENDESUMINISTRO.COMPANIA,  
    D_ORDENDESUMINISTRO.ORDENDESUMINISTRO,  
    D_ORDENDESUMINISTRO.CODIGO