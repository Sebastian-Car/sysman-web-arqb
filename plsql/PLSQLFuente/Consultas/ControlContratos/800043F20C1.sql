WITH TMP_CONTROLCONTRATOS AS(
SELECT DISTINCT
 
TERCERO.NIT AS TERCEROID
FROM ((ORDENDECOMPRA
 INNER JOIN TERCERO ON 
 ORDENDECOMPRA.TERCERO = TERCERO.NIT) 
INNER JOIN TIPOORDENDECOMPRA ON 
ORDENDECOMPRA.CLASEORDEN = TIPOORDENDECOMPRA.CODIGO AND 
ORDENDECOMPRA.COMPANIA = TIPOORDENDECOMPRA.COMPANIA) 
WHERE EXTRACT( YEAR FROM ORDENDECOMPRA.FECHA) = EXTRACT (YEAR FROM SYSDATE) 
)

SELECT DISTINCT TERCERO.COMPANIA, 
    TERCERO.NIT, 
    TERCERO.NOMBRE,
    TERCERO.TIPO_CONTRATISTA, 
    CONSORCIADOS.NIT AS NITCONSOSORCIADO,
    CONSORCIADOS.NOMBRE AS NOMBRECONSORCIADO,
    ORDENDECOMPRA.NUMERO
FROM ((TERCERO 
    INNER JOIN CONSORCIADOS ON 
    TERCERO.SUCURSAL = CONSORCIADOS.SUCURSAL AND 
    TERCERO.COMPANIA = CONSORCIADOS.COMPANIA AND 
    TERCERO.NIT = CONSORCIADOS.NITCONSORCIO)
    INNER JOIN TMP_CONTROLCONTRATOS ON 
    TERCERO.NIT = TMP_CONTROLCONTRATOS.TERCEROID)
    INNER JOIN ORDENDECOMPRA ON 
    TERCERO.NIT = ORDENDECOMPRA.TERCERO AND 
    TERCERO.SUCURSAL = ORDENDECOMPRA.SUCURSAL AND 
    TERCERO.COMPANIA = ORDENDECOMPRA.COMPANIA
WHERE TERCERO.COMPANIA=s$compania$s
  AND (TERCERO.TIPO_CONTRATISTA=2 
   OR TERCERO.TIPO_CONTRATISTA=3)
