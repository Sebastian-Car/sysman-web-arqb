MERGE INTO CONSULTAS FIN USING (SELECT '002000SeguimientosTiempos' INFORME ,TO_CLOB(q'[SELECT TRAMITES.NUMERO,
DEPENDENCIA.NOMBRE DEPENDENCIA,
  TIPOTRAMITES.NOMBRE "MODALIDAD DE SELECCIÓN",
  TRAMITES.DESCRIPCION "OBJETO",
  TRAMITES.CONTRATO_NUM "NUMERO CONTRATO",
  NODOS.NOMBRE "ETAPA",
  CASE D_TRAMITES.ESTADO
    WHEN 4
    THEN 'ACTIVO'
    WHEN 5
    THEN 'INACTIVO'
  END "ACTIVA",
  TO_CHAR(D_TRAMITES.FECHA,'dd/MM/yyyy') "FECHA INICIAL",
  TO_CHAR(D_TRAMITES.FECHA_PASE,'dd/MM/yyyy') "FECHA FINAL",
  PCK_SYSMAN_UTL.FC_DIASMESCOMERCIAL(D_TRAMITES.FECHA, D_TRAMITES.FECHA_PASE) "DURACIÓN EJECUTADA",
  NODOS.TIEMPO_ESTIMADO  "DURACIÓN ESPERADA",
  D_TRAMITES.USUARIO_INTERNO "USUARIO A CARGO"
FROM D_TRAMITES
INNER JOIN NODOS
ON D_TRAMITES.COMPANIA     = NODOS.COMPANIA
AND D_TRAMITES.NODO_DESTINO=NODOS.CODIGO
AND D_TRAMITES.PROCESOS    = NODOS.CODIGO_PROCESO
INNER JOIN TRAMITES
ON D_TRAMITES.NUMERO      = TRAMITES.NUMERO
AND TRAMITES.COMPANIA     = D_TRAMITES.COMPANIA
AND TRAMITES.TIPO_TRAMITE = D_TRAMITES.TIPO_TRAMITE
AND TRAMITES.PROCESOS     = D_TRAMITES.PROCESOS
INNER JOIN TIPOTRAMITES
ON D_TRAMITES.COMPANIA      = TIPOTRAMITES.COMPANIA
AND D_TRAMITES.TIPO_TRAMITE = TIPOTRAMITES.TIPOTRAMITE
AND D_TRAMITES.PROCESOS     = TIPOTRAMITES.PROCESOS
LEFT JOIN DEPENDENCIA
ON TRAMITES.COMPANIA      = DEPENDENCIA.COMPANIA
AND TRAMITES.DEPENDENCIA = DEPENDENCIA.CODIGO
WHERE TO_CHAR(TRAMITES.FECHA,'YYYY')  = s$ano$s
AND D_TRAMITES.COMPANIA     = 's$compania$s' 
AND DEPENDENCIA.CODIGO  BETWEEN s$dependenciaInicial$s and s$dependenciaFinal$s]') CONSULTA, 35 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
COMMIT;