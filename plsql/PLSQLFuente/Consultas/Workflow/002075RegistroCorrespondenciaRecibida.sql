MERGE INTO CONSULTAS FIN USING (SELECT '002075RegistroCorrespondenciaRecibida' INFORME ,TO_CLOB(q'[SELECT
 	TRAMITES.NUMERO AS NUMERO_RADICADO,
DEPENDENCIA.NOMBRE DEPENDENCIA,
TRAMITES.FECHA AS FECHA_RADICADO,
TRAMITES.PROCEDENCIA,
PROCEDENCIA_TRAMITE.NOMBRE AS REMITENTE,
TRAMITES.CIUDAD_ORIGEN,
CIUDAD.NOMBRE AS CIUDAD,
TRAMITES.CANTIDAD_OFICIOS AS FOLIOS,
SYSDATE AS FECHA_DOCUMENTO,
SYSDATE AS FECHA_PASE,
TRAMITES.DESCRIPCION AS ASUNTO,
NODOS.NOMBRE AS ARCHIVAR_EN,
TO_CHAR(TRAMITES.FECHA,'DD/MM/YYYY HH24:MI:SS') AS FIRMA_FECHA_HORA,
TRAMITES.TIPO_POBLACION,
TIPOS.NOMBRE AS NOMBRE_POBLACION
FROM TRAMITES
INNER JOIN NODOS 
ON TRAMITES.COMPANIA = NODOS.COMPANIA
AND TRAMITES.PROCESOS = NODOS.CODIGO_PROCESO
AND TRAMITES.NODO_ORIGEN = NODOS.CODIGO
LEFT JOIN DEPENDENCIA
ON TRAMITES.COMPANIA=DEPENDENCIA.COMPANIA
AND TRAMITES.DEPENDENCIA=DEPENDENCIA.CODIGO
INNER JOIN CIUDAD
 ON TRAMITES.CIUDAD_ORIGEN = CIUDAD.CODIGO
AND TRAMITES.DEPARTAMENTO_ORIGEN = CIUDAD.DEPARTAMENTO
AND TRAMITES.PAIS_ORIGEN = CIUDAD.PAIS
LEFT JOIN PROCEDENCIA_TRAMITE
 ON TRAMITES.COMPANIA = PROCEDENCIA_TRAMITE.COMPANIA
AND TRAMITES.PROCEDENCIA = PROCEDENCIA_TRAMITE.CODIGO
LEFT JOIN TIPOS
 ON TRAMITES.TIPO_POBLACION = TIPOS.ID
WHERE TRAMITES.COMPANIA = s$compania$s
AND TRAMITES.NUMERO BETWEEN s$numeroInicial$s AND s$numeroFinal$s
AND TO_CHAR(TRAMITES.FECHA_REAL,'DD/MM/YYYY') BETWEEN 's$fechaInicial$s' AND 's$fechaFinal$s'
AND TO_CHAR(TRAMITES.HORA,'HH24:MM:SS') BETWEEN 's$horaInicial$s' AND 's$horaFinal$s'
ORDER BY TRAMITES.NUMERO]') CONSULTA, 35 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
COMMIT;