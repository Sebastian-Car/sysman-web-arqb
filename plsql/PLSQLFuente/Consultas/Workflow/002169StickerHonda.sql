SELECT DISTINCT COMPANIA.NOMBRE COMPANIA, 
TRAMITES.NUMERO, 
TRAMITES.DESCRIPCION, 
TO_CHAR(TRAMITES.HORA, 'HH24:MI:SS') HORA , 
TO_CHAR(TRAMITES.FECHA,'DD/MM/YYYY') FECHA, 
TRAMITES.CANTIDAD_OFICIOS FOLIOS, 
TRAMITES.CANTIDAD_ANEXOS ANEXOS, 
TRAMITES.CLASE_ANEXOS, 
TRAMITES.PROCESOS||'-'||TRAMITES.NODO_ACTUAL||'-'||TRAMITES.SERIE_DOCUMENTAL CODIGODEBARRAS_BIN,
PROCEDENCIA_TRAMITE.CODIGO,
PROCEDENCIA_TRAMITE.NOMBRE DESTINO,
TRAMITES.CREATED_BY ORIGEN,
DEPENDENCIA.NOMBRE DEPENDENCIA,
PROCESOS.NOMBRE PROCESO,
CONCAT(CONCAT(USUARIO.NOMBRE1,' '),USUARIO.APELLIDO1) RECIBIDO,
TIPOTRAMITES.NOMBRE TIPO_TRAMITE
FROM TRAMITES 
INNER JOIN COMPANIA 
ON TRAMITES.COMPANIA= COMPANIA.CODIGO 
LEFT JOIN PROCEDENCIA_TRAMITE
ON TRAMITES.COMPANIA = PROCEDENCIA_TRAMITE.COMPANIA
AND TRAMITES.PROCEDENCIA = PROCEDENCIA_TRAMITE.CODIGO
INNER JOIN DEPENDENCIA
ON TRAMITES.DEPENDENCIA = DEPENDENCIA.CODIGO
INNER JOIN PROCESOS
ON TRAMITES.PROCESOS = PROCESOS.CODIGO
INNER JOIN USUARIO
ON TRAMITES.CREATED_BY = USUARIO.CODIGO
INNER JOIN TIPOTRAMITES
ON TRAMITES.PROCESOS = TIPOTRAMITES.PROCESOS
AND TRAMITES.TIPO_TRAMITE = TIPOTRAMITES.TIPOTRAMITE
WHERE TRAMITES.COMPANIA = s$compania$s
AND TRAMITES.PROCESOS ='s$procesos$s'
AND TRAMITES.NUMERO=s$numero$s
AND TRAMITES.TIPO_TRAMITE = s$tipotramites$s