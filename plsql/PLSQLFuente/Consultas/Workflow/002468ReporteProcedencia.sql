MERGE INTO CONSULTAS FIN USING (SELECT '002468ReporteProcedencia' INFORME ,TO_CLOB(q'[SELECT DISTINCT
    T.NUMERO             TRAMITE,
    '(' || T.PROCESOS || ')' || P.NOMBRE || ' - ' || TT.NOMBRE AS PROCESO,
    TO_CHAR(T.FECHA_REAL, 'DD/MM/YYYY') FECHA_RADICADO,
    N.NOMBRE             NOMBRE_ETAPA,
    D.NOMBRE             NOMBRE_DEPENDENCIA,
    T.DESCRIPCION,
    TRIM(U.NOMBRE1 || ' ' || U.NOMBRE2 || ' ' || U.APELLIDO1 || ' ' || U.APELLIDO2) AS RESPONSABLE,
    NVL(PT.DOCUMENTO, PT.NIT) AS DOCUMENTO,
    PT.NOMBRE            NOMBRE_TERCERO,
    PT.DIRECCION,
    PT.DIRECCIONE_MAIL   EMAIL,
    NVL(PT.TELEFONO,PT.CONTACTO) TELEFONO,
    CASE WHEN N.ENVIO_CORRES IN (0) THEN 'NO' ELSE 'SI' END ENVIO_CORRESPONDENCIA,
    CASE WHEN N.ENVIO_CORRES IN (0) THEN '' ELSE TO_CHAR(DT.FECHA, 'DD/MM/YYYY') END FECHA_RESPUESTA,
    CASE WHEN N.ENVIO_CORRES = 0 THEN '' ELSE TRIM(U.NOMBRE1 || ' ' || U.NOMBRE2 || ' ' || U.APELLIDO1 || ' ' || U.APELLIDO2) END AS USUARIO
FROM
    TRAMITES              T
    INNER JOIN PROCESOS              P ON T.COMPANIA = P.COMPANIA
                             AND T.PROCESOS = P.CODIGO
    INNER JOIN NODOS                 N ON T.COMPANIA = N.COMPANIA
                          AND T.PROCESOS = N.CODIGO_PROCESO
                          AND T.NODO_ACTUAL = N.CODIGO
    LEFT JOIN D_TRAMITES            DT ON T.COMPANIA = DT.COMPANIA
                               AND T.PROCESOS = DT.PROCESOS
                               AND T.TIPO_TRAMITE = DT.TIPO_TRAMITE
                               AND T.NUMERO = DT.NUMERO
                               AND T.NODO_ACTUAL = DT.NODO_DESTINO
    LEFT JOIN DEPENDENCIA           D ON T.COMPANIA = D.COMPANIA
                               AND T.DEPENDENCIA = D.CODIGO
    LEFT JOIN PROCEDENCIA_TRAMITE   PT ON T.COMPANIA = PT.COMPANIA
                                        AND T.PROCEDENCIA = PT.CODIGO
    INNER JOIN TIPOTRAMITES          TT ON T.COMPANIA = TT.COMPANIA
                                  AND T.PROCESOS = TT.PROCESOS
                                  AND T.TIPO_TRAMITE = TT.TIPOTRAMITE
    LEFT JOIN USUARIO               U ON T.USUARIO_INTERNO = U.CODIGO
WHERE
    T.COMPANIA = s$compania$s
    AND (
        (PT.DOCUMENTO  BETWEEN 's$terceroInicial$s' AND 's$terceroFinal$s')
        OR (PT.DOCUMENTO IS NULL AND PT.NIT  BETWEEN 's$terceroInicial$s' AND 's$terceroFinal$s')
    )
    AND T.PROCESOS BETWEEN 's$procesoInicial$s' AND 's$procesoFinal$s'
s$filtro$s ORDER BY 1]') CONSULTA, 35 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, 'MROSERO' MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
