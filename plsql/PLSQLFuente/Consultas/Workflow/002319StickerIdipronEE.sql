select 
COMPANIA, NUMERO, FECHA_HORA, DESTINO, ASUNTO, VT_FOLIOS as  FOLIOS, CODIGODEBARRAS_BIN, ORIGEN, 
 VT_ANEXO || ' - '|| VT_CLASEANEXO as ANEXOS
from(
SELECT COMPANIA.NOMBRE COMPANIA, 
TRAMITES.NUMERO,  
TO_CHAR(TRAMITES.FECHA,'DD/MM/YYYY') || '-'|| TO_CHAR(TRAMITES.HORA, 'HH24:MI:SS') FECHA_HORA,
PROCEDENCIA_TRAMITE.NOMBRE DESTINO,
TRAMITES.DESCRIPCION ASUNTO,
TRAMITES.NUMERO CODIGODEBARRAS_BIN,
DEPENDENCIA.NOMBRE ORIGEN,
dtv.CODIGO_NODO_VARIABLE as variable,
dtv.valor_texto as valor
FROM TRAMITES
INNER JOIN PROCEDENCIA_TRAMITE 
ON PROCEDENCIA_TRAMITE.COMPANIA = TRAMITES.COMPANIA
AND PROCEDENCIA_TRAMITE.CODIGO = TRAMITES.PROCEDENCIA
INNER JOIN COMPANIA 
ON TRAMITES.COMPANIA= COMPANIA.CODIGO 
LEFT JOIN DEPENDENCIA
ON TRAMITES.COMPANIA = DEPENDENCIA.COMPANIA
AND TRAMITES.DEPENDENCIA = DEPENDENCIA.CODIGO
LEFT JOIN USUARIO
ON TRAMITES.CREATED_BY = USUARIO.CODIGO
LEFT JOIN USUARIO_DEPENDENCIA
ON TRAMITES.COMPANIA = USUARIO_DEPENDENCIA.COMPANIA
AND TRAMITES.CREATED_BY = USUARIO_DEPENDENCIA.USUARIO
LEFT JOIN DEPENDENCIA DEPE
ON DEPE.COMPANIA = USUARIO_DEPENDENCIA.COMPANIA
AND DEPE.CODIGO = USUARIO_DEPENDENCIA.DEPENDENCIA
inner joiN D_TRAMITE_VARIABLES dtv
 on TRAMITES.compania = dtv.compania
and TRAMITES.procesos = dtv.codigo_proceso
and TRAMITES.nodo_actual = DTv.CODIGO_NODO
anD TRAMITES.numerO = Dtv.NUMERO_TRAMITE
anD TRAMITES.TIPO_TRAMITE = dtv.TIPO_TRAMITE
WHERE TRAMITES.COMPANIA = s$compania$s
AND TRAMITES.PROCESOS ='s$procesos$s'
AND TRAMITES.NUMERO=s$numero$s
AND TRAMITES.TIPO_TRAMITE = s$tipotramites$s
) TRAMITES
PIVOT (MAX(VALOR)
FOR VARIABLE
IN ( '1010140' as "VT_FOLIOS", 
     '1010141' as "VT_ANEXO", 
     '1010142' as "VT_CLASEANEXO")
)
;