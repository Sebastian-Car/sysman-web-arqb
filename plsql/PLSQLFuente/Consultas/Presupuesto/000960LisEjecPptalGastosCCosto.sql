WITH LISEJECPPTALGASTOSC AS (
SELECT SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,
  NVL(PLAN_PRESUPUESTAL.CENTRO_COSTO,'')                     		 CCOSTO,
  PLAN_PRESUPUESTAL.NOMBRE,
  PLAN_PRESUPUESTAL.MOVIMIENTO,
  SALDO_PLAN_PPTAL.ID,
  SALDO_PLAN_PPTAL.CODIGO CUENTA,
  SUM(SALDO_PLAN_PPTAL.ADICION)                              		ADICION,
  SUM(SALDO_PLAN_PPTAL.REDUCCION)                             		REDUCCION,
  SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD)                       		DISPONIBILIDADES,
  SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)                          		REGCONT,
  SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)                       		REGNOCONT,
  SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)                        		MODREGCONT,
  SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT)                      		MODREGNOCONT,
  SUM(EJE_PPT_DEBITO   -EJE_PPT_CREDITO)                      		PAGOSACUMULADOS,
  SUM(REGISTRO_OBLIGACION+MODIF_REGISTRO_OBLIGACION)         	REGISTROOBLIGACION, 
  SUM(CASE PLAN_PRESUPUESTAL.NATURALEZA 
    WHEN 'D' 
    THEN APROPIACION_DEBITO-APROPIACION_CREDITO 
    ELSE APROPIACION_CREDITO-APROPIACION_DEBITO END)          	APROPIADO,
  SUM(CASE PLAN_PRESUPUESTAL.NATURALEZA 
        WHEN 'D' 
        THEN TRASLADO_DEBITO-TRASLADO_CREDITO 
        ELSE TRASLADO_CREDITO-TRASLADO_DEBITO END)            		TRASLADO,
  SUM(CASE PLAN_PRESUPUESTAL.NATURALEZA
      WHEN 'D' 
      THEN APLAZAM_DEBITO -APLAZAM_CREDITO
      ELSE APLAZAM_CREDITO-APLAZAM_DEBITO END)                		APLAZAMIENTO
FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
INNER JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL ON
  PLAN_PRESUPUESTAL.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA AND
  PLAN_PRESUPUESTAL.ANO      = SALDO_PLAN_PPTAL.ANO AND
  PLAN_PRESUPUESTAL.ID       = SALDO_PLAN_PPTAL.ID
WHERE SALDO_PLAN_PPTAL.COMPANIA = s$compania$s
  AND SALDO_PLAN_PPTAL.ANO = s$anio$s
  AND PLAN_PRESUPUESTAL.CENTRO_COSTO          
  BETWEEN 's$centroCostoInicial$s' AND 's$centroCostoFinal$s'
 
    AND SALDO_PLAN_PPTAL.ID
  BETWEEN 's$cuentaIncial$s' AND 's$cuentaFinal$s'
    AND PLAN_PRESUPUESTAL.NATURALEZA ='D'
  AND SALDO_PLAN_PPTAL.MES <= s$mes$s    
GROUP BY SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,
  NVL(PLAN_PRESUPUESTAL.CENTRO_COSTO,''),
  PLAN_PRESUPUESTAL.NOMBRE,
  PLAN_PRESUPUESTAL.MOVIMIENTO,
  PLAN_PRESUPUESTAL.CENTRO_COSTO,
  SALDO_PLAN_PPTAL.ID,
  SALDO_PLAN_PPTAL.CODIGO,
  PLAN_PRESUPUESTAL.NATURALEZA
)


SELECT L.COMPANIA,
  L.CUENTA,
  (SALDO_PLAN_PPTAL.EJE_PPT_DEBITO-SALDO_PLAN_PPTAL.EJE_PPT_CREDITO) AS PAGOSMES,
  L.CCOSTO,
  CENTRO_COSTO.NOMBRE  NOMBRECENTROCOSTO,
  L.MOVIMIENTO,
  L.APROPIADO,
  L.DISPONIBILIDADES,
  L.REGISTROOBLIGACION,
  L.PAGOSACUMULADOS,
  L.NOMBRE,
  SALDO_PLAN_PPTAL.MES,
  L.PAGOSACUMULADOS-(SALDO_PLAN_PPTAL.EJE_PPT_DEBITO-SALDO_PLAN_PPTAL.EJE_PPT_CREDITO)                                             		 	PAGOSANTERIORES,
  L.REGISTROOBLIGACION-L.PAGOSACUMULADOS                                                                                            					CUENTASPORPAGAR,
  L.ADICION+L.REDUCCION+L.TRASLADO+L.APLAZAMIENTO                                                                                  				 	MODIFICACIONES,
  L.REGCONT+L.REGNOCONT+L.MODREGCONT+L.MODREGNOCONT                                                                                				 	RESERVAS,
  L.DISPONIBILIDADES-(L.REGCONT+L.REGNOCONT+L.MODREGCONT+L.MODREGNOCONT)                                                           			 	TOTAL,
  (L.APROPIADO+L.ADICION+L.REDUCCION+L.TRASLADO+L.APLAZAMIENTO)                                                                    			 	TOTALAPROPIADO,
  (L.APROPIADO+L.ADICION+L.REDUCCION+L.TRASLADO+L.APLAZAMIENTO)-(L.DISPONIBILIDADES-(L.REGCONT+L.REGNOCONT+L.MODREGCONT+L.MODREGNOCONT))     SALDO,
  L.DISPONIBILIDADES - (L.REGCONT+L.REGNOCONT+L.MODREGCONT+L.MODREGNOCONT)                                                          SALDODEDISPONIBILIDADES,
  (L.APROPIADO+L.ADICION+L.REDUCCION+L.TRASLADO+L.APLAZAMIENTO) - (L.REGCONT+L.REGNOCONT+L.MODREGCONT+L.MODREGNOCONT)               	SALDODEAPROPIACION,
  CASE WHEN (L.APROPIADO+L.ADICION+L.REDUCCION+L.TRASLADO+L.APLAZAMIENTO) >0 
  THEN ((L.REGCONT+L.REGNOCONT+L.MODREGCONT+L.MODREGNOCONT)/(L.APROPIADO+L.ADICION+L.REDUCCION+L.TRASLADO+L.APLAZAMIENTO))*100
  ELSE 0  END                                                                                                                        							 COMPR
FROM LISEJECPPTALGASTOSC L
INNER JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL ON
  L.COMPANIA  = SALDO_PLAN_PPTAL.COMPANIA AND
  L.ANO       = SALDO_PLAN_PPTAL.ANO AND
  L.ID        = SALDO_PLAN_PPTAL.ID
INNER JOIN CENTRO_COSTO ON 
  L.COMPANIA  = CENTRO_COSTO.COMPANIA AND 
  L.CCOSTO    = CENTRO_COSTO.CODIGO AND
  L.ANO       = CENTRO_COSTO.ANO
WHERE SALDO_PLAN_PPTAL.COMPANIA = s$compania$s
AND SALDO_PLAN_PPTAL.MES = s$mes$s
AND LENGTH(SALDO_PLAN_PPTAL.CODIGO) < s$nivel$s
ORDER BY L.COMPANIA,
  L.ANO,
  L.ID
