SELECT CARGOCOMPROBANTE CARGO,
CUENTA AS CODIGO_CUENTA,
FECHA,
CUENTA,
DESCRIPCION,
COMPROBANTE, 
TIPO,
SUCURSAL_COM AS SUCURSAL,
COMPROBANTE AS NUMERO,
TERCERO_COM AS NOMBRETERCERO1,
TERCERO_NOM AS NOMBRET,
CASE WHEN V_DETALLE_AUXILIAR_PPTAL.VALOR_DEBITO IN (0) 
      THEN V_DETALLE_AUXILIAR_PPTAL.VALOR_CREDITO
      ELSE V_DETALLE_AUXILIAR_PPTAL.VALOR_DEBITO
END VALORCOMPROBANTE,
VALOR_DEBITO,
VALOR_CREDITO AS CONTRACREDITO,
CUENTANOMBRE  TOTALNOMBRE,
CASE
    WHEN NATURALEZA='D'
    THEN (VALOR_DEBITO  + MODIFICACION_DEBITO - MODIFICACION_CREDITO) - DEBITO_AFECTADO
    ELSE (VALOR_CREDITO + MODIFICACION_CREDITO - MODIFICACION_DEBITO) - CREDITO_AFECTADO
  END AS SALDOCOMPROBANTE,
TEXTO AS TXT,
FECHA_VCN_DOC,
NOMBRE_DEP NOMBRED,
CREATED_BY AS CREADO
FROM V_DETALLE_AUXILIAR_PPTAL
WHERE COMPANIA          = s$compania$s
AND ANO                 = s$ano$s
AND TIPO                = 's$tipoCpte$s'
AND COMPROBANTE BETWEEN s$numeroPptoInicial$s AND s$numeroPptoFinal$s
