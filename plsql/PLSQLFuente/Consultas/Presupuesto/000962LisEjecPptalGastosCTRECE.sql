WITH LISEJECPPTALGASTOS_CTRECE AS(
SELECT SALDO_PLAN_PPTAL.COMPANIA, 
       SALDO_PLAN_PPTAL.ANO,
       PLAN_PRESUPUESTAL.NOMBRE,
       SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN APROPIACION_DEBITO-APROPIACION_CREDITO
                ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
                END) APROPIADO,
       SUM(SALDO_PLAN_PPTAL.ADICION) ADICION,
       SUM(SALDO_PLAN_PPTAL.REDUCCION) REDUCCION,
       SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN TRASLADO_DEBITO-TRASLADO_CREDITO
                ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
                END) TRASLADO,
       SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN APLAZAM_DEBITO -APLAZAM_CREDITO
                ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
                END) APLAZAMIENTO,
       SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD) DISPONIBILIDADES,
       SUM(SALDO_PLAN_PPTAL.REG_CONTRACT) REGCONT,
       SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT) REGNOCONT,
       SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT) MODREGCONT,
       SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT) MODREGNOCONT,
       SUM(REGISTRO_OBLIGACION+MODIF_REGISTRO_OBLIGACION) REGISTROOBLIGACION,
       SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)
       +SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)
       +SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)
       +SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT) RESERVAS,
       SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD)-SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)
       +SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)
       +SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)
       +SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT) TOTAL,
       SUM(EJE_PPT_DEBITO-EJE_PPT_CREDITO) PAGOSACUMULADOS,
       (SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN APROPIACION_DEBITO-APROPIACION_CREDITO
                ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
                END)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.REDUCCION)+SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN TRASLADO_DEBITO-TRASLADO_CREDITO
                ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
                END)+SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN APLAZAM_DEBITO -APLAZAM_CREDITO
                ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
                END))-(SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD)-SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)
       +SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)
       +SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)
       +SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT)) SALDO,
       PLAN_PRESUPUESTAL.MOVIMIENTO,
       SALDO_PLAN_PPTAL.ID CUENTA,
       PLAN_PRESUPUESTAL.CODIGO,
       PLAN_PRESUPUESTAL.AUXILIAR,
       (SUM(SALDO_PLAN_PPTAL.ADICION)
        +SUM(SALDO_PLAN_PPTAL.REDUCCION)
        +SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN TRASLADO_DEBITO-TRASLADO_CREDITO
                ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
                END)+SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN APLAZAM_DEBITO -APLAZAM_CREDITO
                ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
                END)) MODIFICACIONES,
       SUM(REGISTRO_OBLIGACION+MODIF_REGISTRO_OBLIGACION)-SUM(EJE_PPT_DEBITO-EJE_PPT_CREDITO) CUENTASPORPAGAR,
       CASE WHEN (SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN APROPIACION_DEBITO-APROPIACION_CREDITO
                ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
                END)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.REDUCCION)+SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN TRASLADO_DEBITO-TRASLADO_CREDITO
                ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
                END)+SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN APLAZAM_DEBITO -APLAZAM_CREDITO
                ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
                END))>0
            THEN (SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)
       +SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)
       +SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)
       +SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT)/(SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN APROPIACION_DEBITO-APROPIACION_CREDITO
                ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
                END)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.REDUCCION)+SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN TRASLADO_DEBITO-TRASLADO_CREDITO
                ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
                END)+SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN APLAZAM_DEBITO -APLAZAM_CREDITO
                ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
                END)))*100
            ELSE 0
            END COMPR,
       (SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN APROPIACION_DEBITO-APROPIACION_CREDITO
                ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
                END)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.REDUCCION)+SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN TRASLADO_DEBITO-TRASLADO_CREDITO
                ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
                END)+SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN APLAZAM_DEBITO -APLAZAM_CREDITO
                ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
                END)) TOTALAPROPIADO,
       SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD)-SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)
       +SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)
       +SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)
       +SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT) SaldoDeDisponibilidades,
       (SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN APROPIACION_DEBITO-APROPIACION_CREDITO
                ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
                END)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.REDUCCION)+SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN TRASLADO_DEBITO-TRASLADO_CREDITO
                ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
                END)+SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                THEN APLAZAM_DEBITO -APLAZAM_CREDITO
                ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
                END))-SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)
       +SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)
       +SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)
       +SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT) SALDODEAPROPIACION
FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
 INNER JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
  ON  PLAN_PRESUPUESTAL.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
  AND PLAN_PRESUPUESTAL.ANO      = SALDO_PLAN_PPTAL.ANO
  AND PLAN_PRESUPUESTAL.ID         = SALDO_PLAN_PPTAL.ID  
WHERE SALDO_PLAN_PPTAL.COMPANIA    = s$compania$s
  AND SALDO_PLAN_PPTAL.ANO         = s$anio$s
  AND PLAN_PRESUPUESTAL.NATURALEZA = 'D'
  AND SALDO_PLAN_PPTAL.MES         BETWEEN 0 AND s$mesInicial$s
  AND SALDO_PLAN_PPTAL.CODIGO      BETWEEN 's$cuentaInicial$s'      AND 's$cuentaFinal$s' 
GROUP BY SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,
  PLAN_PRESUPUESTAL.NOMBRE,
  PLAN_PRESUPUESTAL.MOVIMIENTO,
  SALDO_PLAN_PPTAL.ID,
  PLAN_PRESUPUESTAL.CODIGO,
  PLAN_PRESUPUESTAL.AUXILIAR,
  PLAN_PRESUPUESTAL.NATURALEZA)
SELECT
  LISEJECPPTALGASTOS_CTRECE.CUENTA, 
  LISEJECPPTALGASTOS_CTRECE.COMPANIA, 
  CASE WHEN LISEJECPPTALGASTOS_CTRECE.RESERVAS IN (0)
       THEN 0 
       ELSE (LISEJECPPTALGASTOS_CTRECE.PAGOSACUMULADOS/LISEJECPPTALGASTOS_CTRECE.RESERVAS)*100 
       END PAGOSVSCOMPRO, 
  LISEJECPPTALGASTOS_CTRECE.MODIFICACIONES MODIFICACIONES1, 
  SALDO_PLAN_PPTAL.ADICION+SALDO_PLAN_PPTAL.REDUCCION+(SALDO_PLAN_PPTAL.TRASLADO_DEBITO-SALDO_PLAN_PPTAL.TRASLADO_CREDITO) TOTALMODIFICACIONESMES,
  SALDO_PLAN_PPTAL.TRASLADO_DEBITO-SALDO_PLAN_PPTAL.TRASLADO_CREDITO TRASLADOMES, 
  SALDO_PLAN_PPTAL.REDUCCION REDUCCIONMES, 
  SALDO_PLAN_PPTAL.ADICION ADICIONMES, 
  LISEJECPPTALGASTOS_CTRECE.CODIGO,
  LISEJECPPTALGASTOS_CTRECE.NOMBRE,
  LISEJECPPTALGASTOS_CTRECE.TOTALAPROPIADO TOTALAPROPIADO1, 
  LISEJECPPTALGASTOS_CTRECE.RESERVAS, 
  CASE WHEN LISEJECPPTALGASTOS_CTRECE.TOTALAPROPIADO=0 
       THEN 0
       ELSE (LISEJECPPTALGASTOS_CTRECE.PAGOSACUMULADOS/LISEJECPPTALGASTOS_CTRECE.TOTALAPROPIADO)*100 
       END APROVSPAGOS, 
  CASE WHEN LISEJECPPTALGASTOS_CTRECE.TOTALAPROPIADO=0 
       THEN 0
       ELSE (LISEJECPPTALGASTOS_CTRECE.RESERVAS/LISEJECPPTALGASTOS_CTRECE.TOTALAPROPIADO)*100 
       END APROVSCOMPRO, 
  (SALDO_PLAN_PPTAL.EJE_PPT_DEBITO-SALDO_PLAN_PPTAL.EJE_PPT_CREDITO) PAGOSMES1, 
  LISEJECPPTALGASTOS_CTRECE.PAGOSACUMULADOS PAGOSACUMULADOS1, 
  LISEJECPPTALGASTOS_CTRECE.APROPIADO APROPIADO1, 
  SALDO_PLAN_PPTAL.REG_CONTRACT+SALDO_PLAN_PPTAL.MODIF_REG_CONT+SALDO_PLAN_PPTAL.REG_NO_CONTRACT+SALDO_PLAN_PPTAL.MODIF_REG_NOCONT RESERVASMES 
FROM LISEJECPPTALGASTOS_CTRECE 
 INNER JOIN V_SALDO_PLAN_PPTAL  SALDO_PLAN_PPTAL
 
  ON  LISEJECPPTALGASTOS_CTRECE.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
  AND LISEJECPPTALGASTOS_CTRECE.ANO      = SALDO_PLAN_PPTAL.ANO
  AND LISEJECPPTALGASTOS_CTRECE.CUENTA   = SALDO_PLAN_PPTAL.ID 
WHERE SALDO_PLAN_PPTAL.MES = s$mesInicial$s
