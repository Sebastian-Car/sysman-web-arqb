SELECT COMPROBANTE,
  CUENTA CODIGO_CUENTA,
  TIPO COMPROBANTE_PPTAL_TIPO,
  TIPO_COMPROBANTE_NOM NOMBRETIPOCOMPRO,
  CUENTANOMBRE NOMBREPP ,
  CUENTA,
  DESCRIPCION OBJ,
  NVL(TEXTO,' ') TEXTO,
  VALOR_DEBITO,
  TERCERO_COM_NOM NOMBRETERCERO1,
  SALDO,
  FECHA_COM FECHA ,
  TERCERO_COM TERCERO,
  SUCURSAL_COM SUCURSAL,
  CREATED_BY ELABORO
FROM V_DETALLE_AUXILIAR_PPTAL
WHERE COMPANIA          = s$compania$s
AND ANO                 = s$ano$s
AND TIPO                = 's$tipoCpte$s'
AND COMPROBANTE BETWEEN s$numeroPptoInicial$s AND s$numeroPptoFinal$s
