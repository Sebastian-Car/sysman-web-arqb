				DIA,
				ANO,
				COMPROBANTE,
				TIPO_CPTE, 
				CLASE,
				VALORTOTAL,
				VALOR,
				ORDENCOMP,
				CLASECOMP,
				CLASELETRAS,
				NOCONDICIONADA,
				TOTALCDP,
				TOTALCOMPROMISOS,
				TOTALPAGOS,
				DESCRIPCION,
				NOMBRE,
				NIVEL1,
				NIVEL2,
				NIVEL3,
				NIVEL4,
				NIVEL5,
				NIVEL6,
				NIVEL7,
				ID, 
				CODIGO, 
				NATURALEZA, 
				TRUNC(FECHA) FECHA, 

				DESTINO, 
				DESTINONOM,
				NRO_DOCUMENTO,
				TIPODOCUMENTO,
				COMPROMETER,
				APROPIACIONINI,
				TERCERONOMBRE,
                                                                                     MODIFICACIONES,
				DETALLE,
					CASE WHEN DETALLE = 1 
				THEN CASE WHEN CLASELETRAS = 'APNC' THEN NOCONDICIONADA ELSE TOTALCDP END  
				ELSE NOCONDICIONADA - TOTALCDP
				END SALDODISPONIBLE ,  CASE WHEN DETALLE = 1 
        THEN CASE WHEN CLASELETRAS = 'ACPN' THEN VALORTOTAL ELSE  0 END + NOCONDICIONADA 
        ELSE NOCONDICIONADA 
        END APROPIACIONFINAL FROM( SELECT 
  DETALLE_COMPROBANTE_PPTAL.MES,
  DETALLE_COMPROBANTE_PPTAL.DIA,
  DETALLE_COMPROBANTE_PPTAL.ANO,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
  TIPO_COMPROBPP.CLASE,
  (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) VALORTOTAL,
  ABS(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) VALOR,
  DECODE(TIPO_COMPROBPP.CLASE,'DIS',1,'ADD',2,'DMD',3,'RES',4,'ADR',5,'DMR',6,'REO',7,'ARO',8,'DRO',9,'EGR',10,'AEG',11,'DEG',12,13) ORDENCOMP,
  CASE WHEN TIPO_COMPROBPP.CLASE IN ('DIS','DMD','ADD') 
       THEN '11' ELSE CASE WHEN TIPO_COMPROBPP.CLASE IN ('RES','DMR','ADR') THEN '13' ELSE CASE WHEN TIPO_COMPROBPP.CLASE IN ('REO','MRO','ARO','DRO') 
                           THEN '15' ELSE CASE WHEN TIPO_COMPROBPP.CLASE IN ('EGR','DEG','AEG') THEN '17' ELSE CASE WHEN TIPO_COMPROBPP.CLASE IN ('TRA','ADC','RED','APL','APR') THEN '10' ELSE '' END  END END END END CLASECOMP,
  CASE WHEN TIPO_COMPROBPP.CLASE IN ('DIS','DMD','ADD') THEN 'CDP' ELSE CASE WHEN TIPO_COMPROBPP.CLASE IN ('RES','DMR','ADR') THEN 'COM' ELSE CASE WHEN TIPO_COMPROBPP.CLASE IN ('REO','MRO','ARO','DRO') 
                           THEN 'OB' ELSE CASE WHEN TIPO_COMPROBPP.CLASE IN ('EGR','DEG','AEG') THEN 'P' ELSE CASE WHEN TIPO_COMPROBPP.CLASE IN ('TRA','ADC','RED','APL','APR') THEN 'APNC' ELSE ''   END END END END END CLASELETRAS,  
  ACUMULADOS.APROPIACIONVIGENTE NOCONDICIONADA,
  ACUMULADOS.DISPONIBILIDADACUM TOTALCDP,
  ACUMULADOS.COMPROMISOSACUM TOTALCOMPROMISOS,
  ACUMULADOS.PAGOSACUM TOTALPAGOS,
  COMPROBANTE_PPTAL.DESCRIPCION,
  V_PLAN_PRESUPUESTAL.NOMBRE,
  V_PLAN_PRESUPUESTAL.NIVEL1,
  V_PLAN_PRESUPUESTAL.NIVEL2,
  V_PLAN_PRESUPUESTAL.NIVEL3,
  V_PLAN_PRESUPUESTAL.NIVEL4,
  V_PLAN_PRESUPUESTAL.NIVEL5,
  V_PLAN_PRESUPUESTAL.NIVEL6,
  V_PLAN_PRESUPUESTAL.NIVEL7,
  V_PLAN_PRESUPUESTAL.ID, 
  V_PLAN_PRESUPUESTAL.CODIGO, 
  V_PLAN_PRESUPUESTAL.NATURALEZA, 
  DETALLE_COMPROBANTE_PPTAL.FECHA, 
  V_PLAN_PRESUPUESTAL.DESTINO, 
  DECODE(V_PLAN_PRESUPUESTAL.DESTINO,'F','Funcionamiento',
                                     'I','Inversión',
                                     'S','Servicio de la Deuda') DESTINONOM,
  COMPROBANTE_PPTAL.NRO_DOCUMENTO NRO_DOCUMENTO,
  COMPROBANTE_PPTAL.TIPO_DOCUMENTO TIPODOCUMENTO,
  CASE WHEN TIPO_CPTE = 'RES' THEN (CASE WHEN TIPO_COMPROBPP.CLASE IN ('RES','DMR','ADR') 
                  THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) 
                  ELSE 0 
             END)
             -(CASE WHEN TIPO_COMPROBPP.CLASE IN ('EGR','DEG','AEG') 
                    THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) 
                    ELSE 0 
               END) 
       ELSE 0 
  END COMPROMETER, 
  NVL(APROPIACIONESINICIALES.APROPIACIONINICIAL,0) APROPIACIONINI,
  CASE WHEN TIPO_CPTE IN ('ADI','APL','DSM','TRA') 
       THEN ABS(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) ELSE 0 END MODIFICACIONES,
  TERCERO.NOMBRE TERCERONOMBRE,
  1 DETALLE
FROM DETALLE_COMPROBANTE_PPTAL 
  INNER JOIN TIPO_COMPROBPP 
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
  INNER JOIN  V_PLAN_PRESUPUESTAL
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = V_PLAN_PRESUPUESTAL.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO      = V_PLAN_PRESUPUESTAL.ANO
    AND DETALLE_COMPROBANTE_PPTAL.ID       = V_PLAN_PRESUPUESTAL.ID 
    
  INNER JOIN COMPROBANTE_PPTAL 
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = COMPROBANTE_PPTAL.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = COMPROBANTE_PPTAL.TIPO
    AND DETALLE_COMPROBANTE_PPTAL.ANO = COMPROBANTE_PPTAL.ANO 
    AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO
  LEFT JOIN APROPIACIONESINICIALES 
     ON V_PLAN_PRESUPUESTAL.COMPANIA       = APROPIACIONESINICIALES.COMPANIA
    AND V_PLAN_PRESUPUESTAL.ANO            = APROPIACIONESINICIALES.ANO
    AND V_PLAN_PRESUPUESTAL.CODIGO         = APROPIACIONESINICIALES.CODIGO 
    AND V_PLAN_PRESUPUESTAL.TERCERO        = APROPIACIONESINICIALES.TERCERO
    AND V_PLAN_PRESUPUESTAL.SUCURSAL       = APROPIACIONESINICIALES.SUCURSAL
    AND V_PLAN_PRESUPUESTAL.AUXILIAR       = APROPIACIONESINICIALES.AUXILIAR
    AND V_PLAN_PRESUPUESTAL.CENTRO_COSTO   = APROPIACIONESINICIALES.CENTRO_COSTO
    AND V_PLAN_PRESUPUESTAL.REFERENCIA     = APROPIACIONESINICIALES.REFERENCIA
    AND V_PLAN_PRESUPUESTAL.FUENTE_RECURSO = APROPIACIONESINICIALES.FUENTE_RECURSO   
  INNER JOIN TERCERO 
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = TERCERO.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.TERCERO  = TERCERO.NIT
    AND DETALLE_COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL 
   LEFT JOIN ( SELECT
        RESUMENPPTO_P.COMPANIA,
        RESUMENPPTO_P.ANO,
        RESUMENPPTO_P.ID,
        RESUMENPPTO_A.APRDEFINITIVA+RESUMENPPTO_P.ADICION+RESUMENPPTO_P.REDUCCION+RESUMENPPTO_P.TRASLADO+RESUMENPPTO_P.APLAZAMIENTO APROPIACIONVIGENTE,
        (RESUMENPPTO_A.APRDEFINITIVA +RESUMENPPTO_P.ADICION+RESUMENPPTO_P.REDUCCION+RESUMENPPTO_P.TRASLADO+RESUMENPPTO_P.APLAZAMIENTO)-(RESUMENPPTO_A.DISPONIBILIDAD+RESUMENPPTO_P.DISPONIBILIDAD)+(RESUMENPPTO_P.REINTEGRO+RESUMENPPTO_A.REINTEGRO) DISPONIBILIDADACUM,
         RESUMENPPTO_P.REGCONT        +RESUMENPPTO_P.REGNOCONTRACT REGISTROSP,
         RESUMENPPTO_A.REGCONT        +RESUMENPPTO_A.REGNOCONTRACT+RESUMENPPTO_P.REGCONT+RESUMENPPTO_P.REGNOCONTRACT COMPROMISOSACUM,
         RESUMENPPTO_A.EJECUCIONPPT-RESUMENPPTO_A.REINTEGRO+RESUMENPPTO_P.EJECUCIONPPT-RESUMENPPTO_P.REINTEGRO PAGOSACUM
      FROM
        (SELECT V_SALDO_PLAN_PPTAL.COMPANIA,
          V_SALDO_PLAN_PPTAL.ANO,
          V_SALDO_PLAN_PPTAL.ID,
          SUM(
          CASE
            WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D'
            THEN TRASLADO_DEBITO -TRASLADO_CREDITO
            ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
          END) TRASLADO,
          SUM(
          CASE
            WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D'
            THEN EJE_PPT_DEBITO -EJE_PPT_CREDITO
            ELSE EJE_PPT_CREDITO-EJE_PPT_DEBITO
          END) EJECUCIONPPT,
          SUM(
          CASE
            WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D'
            THEN APLAZAM_DEBITO -APLAZAM_CREDITO
            ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
          END) APLAZAMIENTO,
          SUM(V_SALDO_PLAN_PPTAL.ADICION) ADICION,
          SUM(V_SALDO_PLAN_PPTAL.REDUCCION) REDUCCION,
          SUM(V_SALDO_PLAN_PPTAL.DISPONIBILIDAD) DISPONIBILIDAD,
          SUM(V_SALDO_PLAN_PPTAL.REINTEGRO) REINTEGRO,
          SUM(V_SALDO_PLAN_PPTAL.REG_NO_CONTRACT)+SUM(V_SALDO_PLAN_PPTAL.MODIF_REG_NOCONT) REGNOCONTRACT,
          SUM(V_SALDO_PLAN_PPTAL.REG_CONTRACT)   +SUM(V_SALDO_PLAN_PPTAL.MODIF_REG_CONT) REGCONT
        FROM V_PLAN_PRESUPUESTAL
        LEFT JOIN V_SALDO_PLAN_PPTAL
        ON V_PLAN_PRESUPUESTAL.COMPANIA   = V_SALDO_PLAN_PPTAL.COMPANIA
        AND V_PLAN_PRESUPUESTAL.ANO       = V_SALDO_PLAN_PPTAL.ANO
        AND V_PLAN_PRESUPUESTAL.ID        = V_SALDO_PLAN_PPTAL.ID
        WHERE V_SALDO_PLAN_PPTAL.COMPANIA = s$compania$s
        AND V_SALDO_PLAN_PPTAL.ANO        = s$anio$s
        AND V_SALDO_PLAN_PPTAL.ID         BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
        AND V_SALDO_PLAN_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s
          AND V_PLAN_PRESUPUESTAL.NATURALEZA = 'D'
        GROUP BY V_SALDO_PLAN_PPTAL.COMPANIA ,
          V_SALDO_PLAN_PPTAL.ANO ,
          V_SALDO_PLAN_PPTAL.ID ,
          V_PLAN_PRESUPUESTAL.NATURALEZA
        )RESUMENPPTO_P
      LEFT JOIN
        (SELECT V_SALDO_PLAN_PPTAL.COMPANIA,
          V_SALDO_PLAN_PPTAL.ANO,
          V_SALDO_PLAN_PPTAL.ID,
          SUM(
          CASE
            WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D'
            THEN TRASLADO_DEBITO -TRASLADO_CREDITO
            ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
          END) TRASLADO,
          SUM(
          CASE
            WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D'
            THEN EJE_PPT_DEBITO -EJE_PPT_CREDITO
            ELSE EJE_PPT_CREDITO-EJE_PPT_DEBITO
          END) EJECUCIONPPT,
          SUM(
          CASE
            WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D'
            THEN APLAZAM_DEBITO -APLAZAM_CREDITO
            ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
          END) APLAZAMIENTO,
          SUM(V_SALDO_PLAN_PPTAL.ADICION) ADICION,
          SUM(V_SALDO_PLAN_PPTAL.REDUCCION) REDUCCION,
          SUM(
          CASE
            WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D'
            THEN APROPIACION_DEBITO -APROPIACION_CREDITO
            ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
          END)                      +SUM(V_SALDO_PLAN_PPTAL.ADICION)+SUM(V_SALDO_PLAN_PPTAL.REDUCCION)+SUM(
          CASE
            WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D'
            THEN APLAZAM_DEBITO -APLAZAM_CREDITO
            ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
          END)                  +SUM(
          CASE
            WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D'
            THEN TRASLADO_DEBITO -TRASLADO_CREDITO
            ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
          END) APRDEFINITIVA,
          SUM(V_SALDO_PLAN_PPTAL.DISPONIBILIDAD) DISPONIBILIDAD,
          SUM(V_SALDO_PLAN_PPTAL.REINTEGRO) REINTEGRO,
          V_PLAN_PRESUPUESTAL.NATURALEZA,
          SUM(V_SALDO_PLAN_PPTAL.REG_NO_CONTRACT)+SUM(V_SALDO_PLAN_PPTAL.MODIF_REG_NOCONT) REGNOCONTRACT,
          SUM(V_SALDO_PLAN_PPTAL.REG_CONTRACT)   +SUM(V_SALDO_PLAN_PPTAL.MODIF_REG_CONT) REGCONT
        FROM V_PLAN_PRESUPUESTAL
        LEFT JOIN V_SALDO_PLAN_PPTAL
        ON V_PLAN_PRESUPUESTAL.COMPANIA   = V_SALDO_PLAN_PPTAL.COMPANIA
        AND V_PLAN_PRESUPUESTAL.ANO       = V_SALDO_PLAN_PPTAL.ANO
        AND V_PLAN_PRESUPUESTAL.ID        = V_SALDO_PLAN_PPTAL.ID
      WHERE V_SALDO_PLAN_PPTAL.COMPANIA = s$compania$s
        AND V_SALDO_PLAN_PPTAL.ANO        = s$anio$s
        AND V_SALDO_PLAN_PPTAL.ID         BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
        AND V_SALDO_PLAN_PPTAL.MES        < s$mesInicial$s
         AND V_PLAN_PRESUPUESTAL.NATURALEZA = 'D'
        GROUP BY V_SALDO_PLAN_PPTAL.COMPANIA ,
          V_SALDO_PLAN_PPTAL.ANO ,
          V_SALDO_PLAN_PPTAL.ID ,
          V_PLAN_PRESUPUESTAL.NATURALEZA
        )RESUMENPPTO_A ON RESUMENPPTO_P.COMPANIA = RESUMENPPTO_A.COMPANIA
      AND RESUMENPPTO_P.ANO                      = RESUMENPPTO_A.ANO
      AND RESUMENPPTO_P.ID                       = RESUMENPPTO_A.ID) ACUMULADOS 
      ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = ACUMULADOS.COMPANIA
      AND DETALLE_COMPROBANTE_PPTAL.ANO      = ACUMULADOS.ANO
      AND DETALLE_COMPROBANTE_PPTAL.CUENTA       = ACUMULADOS.ID 
  WHERE V_PLAN_PRESUPUESTAL.COMPANIA = s$compania$s  
  AND V_PLAN_PRESUPUESTAL.ANO = s$anio$s  
  AND V_PLAN_PRESUPUESTAL.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  AND 
  V_PLAN_PRESUPUESTAL.NATURALEZA = 'D'
  AND DETALLE_COMPROBANTE_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s  
  AND TIPO_COMPROBPP.CLASE IN ('DIS','DMD','ADD','RES','DMR','ADR','REO','MRO','ARO','DRO','EGR','DEG','AEG','TRA','ADC','RED','APL',' Apr ')
  AND LENGTH(DETALLE_COMPROBANTE_PPTAL.CUENTA) <= s$nivel$s
  UNION ALL 
 SELECT 
