  V_PLAN_PRESUPUESTAL.ID, 
  V_PLAN_PRESUPUESTAL.CODIGO, 
  V_PLAN_PRESUPUESTAL.NATURALEZA, 
  NULL FECHA, 
  V_PLAN_PRESUPUESTAL.DESTINO,
  DECODE(V_PLAN_PRESUPUESTAL.DESTINO,'F','Funcionamiento','I','Inversión','S','Servicio de la Deuda') DESTINONOM,
  NULL NRO_DOCUMENTO,
  '' TIPODOCUMENTO,
  0 COMPROMETER, 
  0 APROPIACIONINI,
  0 MODIFICACIONES,
  NULL TERCERONOMBRE,
  0 DETALLE
FROM V_PLAN_PRESUPUESTAL
  LEFT JOIN  (SELECT DETALLE_COMPROBANTE_PPTAL.COMPANIA, 
                     DETALLE_COMPROBANTE_PPTAL.ANO, 
                     DETALLE_COMPROBANTE_PPTAL.ID
              FROM DETALLE_COMPROBANTE_PPTAL   INNER JOIN TIPO_COMPROBPP 
                 ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA
                AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
              WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA = s$compania$s  
                  AND DETALLE_COMPROBANTE_PPTAL.ANO = s$anio$s  
                  AND DETALLE_COMPROBANTE_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s  
                  AND DETALLE_COMPROBANTE_PPTAL.CUENTA BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
                  AND 
                  TIPO_COMPROBPP.CLASE IN ('DIS','DMD','ADD','RES','DMR','ADR','REO','MRO','ARO','DRO','EGR','DEG','AEG','TRA','ADC','RED','APL',' Apr ')
              ) DET
               ON V_PLAN_PRESUPUESTAL.COMPANIA = DET.COMPANIA
              AND V_PLAN_PRESUPUESTAL.ANO      = DET.ANO
              AND V_PLAN_PRESUPUESTAL.ID        = DET.ID
  INNER JOIN V_SALDO_PLAN_PPTAL 
     ON V_PLAN_PRESUPUESTAL.COMPANIA = V_SALDO_PLAN_PPTAL.COMPANIA
    AND V_PLAN_PRESUPUESTAL.ANO      = V_SALDO_PLAN_PPTAL.ANO 
    AND V_PLAN_PRESUPUESTAL.ID       = V_SALDO_PLAN_PPTAL.ID
WHERE V_PLAN_PRESUPUESTAL.COMPANIA = s$compania$s
  AND V_PLAN_PRESUPUESTAL.ANO = s$anio$s
  AND V_PLAN_PRESUPUESTAL.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  AND 
  V_PLAN_PRESUPUESTAL.NATURALEZA = 'D'
  AND V_PLAN_PRESUPUESTAL.MOVIMIENTO NOT IN(0)
  AND LENGTH(V_PLAN_PRESUPUESTAL.CODIGO) <= s$nivel$s
  AND V_SALDO_PLAN_PPTAL.MES BETWEEN 0 AND s$mesFinal$s
  AND  DET.COMPANIA IS NULL
GROUP BY  V_PLAN_PRESUPUESTAL.COMPANIA,
