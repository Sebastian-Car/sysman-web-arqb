  NULL CLASELETRAS,  
  SUM(CASE WHEN V_PLAN_PRESUPUESTAL . NATURALEZA ='D' 
           THEN APROPIACION_DEBITO - APROPIACION_CREDITO 
           ELSE APROPIACION_CREDITO - APROPIACION_DEBITO 
           END) 
   +SUM(V_SALDO_PLAN_PPTAL.ADICION) 
   +SUM(V_SALDO_PLAN_PPTAL.REDUCCION) 
   +SUM(CASE WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D' 
             THEN TRASLADO_DEBITO-TRASLADO_CREDITO 
             ELSE TRASLADO_CREDITO-TRASLADO_DEBITO 
        END) 
   +SUM(CASE WHEN V_PLAN_PRESUPUESTAL.NATURALEZA ='D' 
             THEN APLAZAM_DEBITO-APLAZAM_CREDITO 
             ELSE APLAZAM_CREDITO-APLAZAM_DEBITO 
        END) NOCONDICIONADA,  
  SUM(V_SALDO_PLAN_PPTAL.DISPONIBILIDAD) TOTALCDP,
  (SUM(V_SALDO_PLAN_PPTAL.REG_CONTRACT) 
  +SUM(V_SALDO_PLAN_PPTAL.MODIF_REG_CONT)) 
  +(SUM(V_SALDO_PLAN_PPTAL.REG_NO_CONTRACT) + SUM(V_SALDO_PLAN_PPTAL.MODIF_REG_NOCONT)) TOTALCOMPROMISOS,
  (SUM(CASE WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D' 
            THEN EJE_PPT_DEBITO-EJE_PPT_CREDITO 
            ELSE EJE_PPT_CREDITO-EJE_PPT_DEBITO 
       END)) 
  -SUM(V_SALDO_PLAN_PPTAL.REINTEGRO) TOTALPAGOS,
  '' DESCRIPCION,
  V_PLAN_PRESUPUESTAL.NOMBRE,
  V_PLAN_PRESUPUESTAL.NIVEL1,
  V_PLAN_PRESUPUESTAL.NIVEL2,  
  V_PLAN_PRESUPUESTAL.NIVEL3,
  V_PLAN_PRESUPUESTAL.NIVEL4,
