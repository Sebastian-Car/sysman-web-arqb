SELECT 
   SYSDATE                          FECHAACTUAL
  ,COMPROBANTE_PPTAL.TIPO
  ,COMPROBANTE_PPTAL.FECHA
  ,COMPROBANTE_PPTAL.TERCERO
  ,COMPROBANTE_PPTAL.SUCURSAL
  ,NVL(TERCERO.NOMBRE,' ')          NOMBRETERCERO
  ,DETALLE_COMPROBANTE_PPTAL.CUENTA CODIGO_CUENTA
  ,PLAN_PRESUPUESTAL.NOMBRE
  ,DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO
  ,DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO
  ,COMPROBANTE_PPTAL.NUMERO         COMPROBANTE
  ,TIPO_COMPROBPP.CODIGO            TIPO_CPTE
 , TIPO_COMPROBPP.NOMBRE   NOMBRECPTE
  ,DETALLE_COMPROBANTE_PPTAL.DESCRIPCION
  ,DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT
  ,DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO
FROM COMPROBANTE_PPTAL
  LEFT JOIN DETALLE_COMPROBANTE_PPTAL
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA    = COMPROBANTE_PPTAL.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO         = COMPROBANTE_PPTAL.ANO
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = COMPROBANTE_PPTAL.TIPO
    AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO
  LEFT JOIN  PLAN_PRESUPUESTAL
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO      = PLAN_PRESUPUESTAL.ANO
    AND DETALLE_COMPROBANTE_PPTAL.CUENTA   = PLAN_PRESUPUESTAL.CODIGO
  LEFT JOIN TERCERO
     ON COMPROBANTE_PPTAL.COMPANIA = TERCERO.COMPANIA
    AND COMPROBANTE_PPTAL.TERCERO  = TERCERO.NIT
    AND COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL
  LEFT JOIN TIPO_COMPROBPP
     ON COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA
    AND COMPROBANTE_PPTAL.TIPO     = TIPO_COMPROBPP.CODIGO
WHERE COMPROBANTE_PPTAL.COMPANIA = s$compania$s
  AND COMPROBANTE_PPTAL.ANO      = s$ano$s
  AND COMPROBANTE_PPTAL.TIPO     = 's$tipoCpte$s' 
  AND COMPROBANTE_PPTAL.NUMERO BETWEEN 's$numeroPptoInicial$s' AND 's$numeroPptoFinal$s'
