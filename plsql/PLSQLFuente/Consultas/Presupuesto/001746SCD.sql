SELECT SOLICITUDDISPONIBILIDAD.NUMERO,
       SOLICITUDDISPONIBILIDAD.FECHA,
       DEPENDENCIA.NOMBRE  NOMBREDEPENDENCIA,
       TERCERO_RESPONBSABLE.NOMBRE  NOMBRERESPONSABLE,
       RESPONSABLE.CARGO  CARGORESPONSABLE,
       SOLICITUDDISPONIBILIDAD.OBJETO,
       SOLICITUDDISPONIBILIDAD.MODALIDAD_SELECCION,
       TIPOADJUDICACION.DESCRIPCION  MODALIDASELECCION,
       SOLICITUDDISPONIBILIDAD.VALOR  VALOSSOLICITADO,
       D_SOLICITUDDISPONIBILIDAD.RUBRO  RUBROPPTAL,
       D_SOLICITUDDISPONIBILIDAD.VALOR VLR_RUBRO,
       RESPONSABLE.NOMBRAMIENTO,
       PLAN_PRESUPUESTAL.NOMBRE  NOMBRERUBRO,
       CASE WHEN DESTINO = 'F' 
        THEN 'FUNCIONAMIENTO' 
        ELSE 'INVERSION' 
       END DESTINOPPTAL
FROM SOLICITUDDISPONIBILIDAD
INNER JOIN DEPENDENCIA 
  ON  SOLICITUDDISPONIBILIDAD.COMPANIA    = DEPENDENCIA.COMPANIA
  AND SOLICITUDDISPONIBILIDAD.DEPENDENCIA = DEPENDENCIA.CODIGO
INNER JOIN RESPONSABLE 
  ON  SOLICITUDDISPONIBILIDAD.COMPANIA = RESPONSABLE.COMPANIA
  AND SOLICITUDDISPONIBILIDAD.TERCERO  = RESPONSABLE.CEDULA
LEFT JOIN TERCERO TERCERO_RESPONBSABLE
  ON  RESPONSABLE.COMPANIA = TERCERO_RESPONBSABLE.COMPANIA
  AND RESPONSABLE.CEDULA   = TERCERO_RESPONBSABLE.NIT
  AND RESPONSABLE.SUCURSAL = TERCERO_RESPONBSABLE.SUCURSAL
LEFT JOIN TIPOADJUDICACION 
  ON  SOLICITUDDISPONIBILIDAD.COMPANIA            = TIPOADJUDICACION.COMPANIA
  AND SOLICITUDDISPONIBILIDAD.MODALIDAD_SELECCION = TIPOADJUDICACION.CODIGO
INNER JOIN D_SOLICITUDDISPONIBILIDAD 
  ON  SOLICITUDDISPONIBILIDAD.COMPANIA = D_SOLICITUDDISPONIBILIDAD.COMPANIA
  AND SOLICITUDDISPONIBILIDAD.NUMERO   = D_SOLICITUDDISPONIBILIDAD.NUMERO
INNER JOIN PLAN_PRESUPUESTAL 
  ON  D_SOLICITUDDISPONIBILIDAD.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
  AND D_SOLICITUDDISPONIBILIDAD.RUBRO    = PLAN_PRESUPUESTAL.CODIGO
  AND D_SOLICITUDDISPONIBILIDAD.ANO      = PLAN_PRESUPUESTAL.ANO
WHERE SOLICITUDDISPONIBILIDAD.COMPANIA = s$compania$s
	AND SOLICITUDDISPONIBILIDAD.NUMERO  = s$numSolicitud$s

