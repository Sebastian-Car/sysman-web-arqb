WITH LISEJECUCIONINGRESOS_PA AS
  (SELECT V_RESUMENPPTO_BASE.COMPANIA,
    V_RESUMENPPTO_BASE.ANO,
    V_RESUMENPPTO_BASE.ID CUENTA,
    SUM(V_RESUMENPPTO_BASE.APROPIADO)    AS APROPIADO,
    SUM(V_RESUMENPPTO_BASE.APLAZAMIENTO) AS APLAZAMIENTO,
    SUM(V_RESUMENPPTO_BASE.TRASLADO)     AS TRASLADO,
    SUM(V_RESUMENPPTO_BASE.ADICION)      AS ADICION,
    SUM(V_RESUMENPPTO_BASE.REDUCCION)    AS REDUCCION,
    V_RESUMENPPTO_BASE.NATURALEZA,
    V_RESUMENPPTO_BASE.CODIGO,
    V_RESUMENPPTO_BASE.NOMBRE,
    SUM(V_RESUMENPPTO_BASE.ADICION) + SUM(V_RESUMENPPTO_BASE.TRASLADO)+SUM(V_RESUMENPPTO_BASE.REDUCCION)+SUM(V_RESUMENPPTO_BASE.APLAZAMIENTO)                              AS MODIFICACIONES,
    SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE)                                                                                                                             AS TOTALPRESUPUESTO,
    SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT        +V_RESUMENPPTO_BASE.MODIF_INGRESOS_EFECTIVO+V_RESUMENPPTO_BASE.MODIF_INGRESOS_PAPELES)                                      AS RECAUDOSACUMULADOS,
    SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE) -SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT +V_RESUMENPPTO_BASE.MODIF_INGRESOS_EFECTIVO+V_RESUMENPPTO_BASE.MODIF_INGRESOS_PAPELES) AS PORRECAUDAR,
    CASE
      WHEN SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE) > 0
      THEN (SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT +V_RESUMENPPTO_BASE.MODIF_INGRESOS_EFECTIVO+V_RESUMENPPTO_BASE.MODIF_INGRESOS_PAPELES)/SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE))*100
      ELSE 0
    END AS PORCRECAUDADO
  FROM V_RESUMENPPTO_BASE
  WHERE V_RESUMENPPTO_BASE.COMPANIA = s$compania$s
  AND V_RESUMENPPTO_BASE.ANO        =s$ano$s
  AND V_RESUMENPPTO_BASE.MES BETWEEN 0 AND s$mes$s
  AND V_RESUMENPPTO_BASE.ID BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
  AND V_RESUMENPPTO_BASE.NATURALEZA='C'
  GROUP BY V_RESUMENPPTO_BASE.COMPANIA,
    V_RESUMENPPTO_BASE.ANO,
    V_RESUMENPPTO_BASE.ID,
    V_RESUMENPPTO_BASE.NATURALEZA,
    V_RESUMENPPTO_BASE.CODIGO,
    V_RESUMENPPTO_BASE.NOMBRE
  ORDER BY V_RESUMENPPTO_BASE.COMPANIA,
    V_RESUMENPPTO_BASE.ANO,
    V_RESUMENPPTO_BASE.ID
  )
SELECT 

LISEJECUCIONINGRESOS_PA.COMPANIA,
  LISEJECUCIONINGRESOS_PA.CUENTA,
  LISEJECUCIONINGRESOS_PA.NOMBRE,
  LISEJECUCIONINGRESOS_PA.APROPIADO,
  LISEJECUCIONINGRESOS_PA.MODIFICACIONES,
  LISEJECUCIONINGRESOS_PA.TOTALPRESUPUESTO,
  LISEJECUCIONINGRESOS_PA.RECAUDOSACUMULADOS,
  LISEJECUCIONINGRESOS_PA.PORRECAUDAR
,
 V_SALDO_PLAN_PPTAL.EJE_PPT_CREDITO -V_SALDO_PLAN_PPTAL.EJE_PPT_DEBITO+V_SALDO_PLAN_PPTAL.MODIF_INGRESOS_EFECTIVO+V_SALDO_PLAN_PPTAL.MODIF_INGRESOS_PAPELES AS RECAUDOSMES
FROM LISEJECUCIONINGRESOS_PA
LEFT JOIN V_SALDO_PLAN_PPTAL
ON LISEJECUCIONINGRESOS_PA.CUENTA                                                                                                                                                                                                                                                                                                                                             = V_SALDO_PLAN_PPTAL.ID
AND LISEJECUCIONINGRESOS_PA.ANO                                                                                                                                                                                                                                                                                                                                               = V_SALDO_PLAN_PPTAL.ANO
AND LISEJECUCIONINGRESOS_PA.COMPANIA                                                                                                                                                                                                                                                                                                                                          = V_SALDO_PLAN_PPTAL.COMPANIA
WHERE LISEJECUCIONINGRESOS_PA.APROPIADO+LISEJECUCIONINGRESOS_PA.ADICION+ABS(LISEJECUCIONINGRESOS_PA.REDUCCION)+ABS(LISEJECUCIONINGRESOS_PA.TRASLADO)+ ABS(LISEJECUCIONINGRESOS_PA.APLAZAMIENTO)+LISEJECUCIONINGRESOS_PA.TOTALPRESUPUESTO+LISEJECUCIONINGRESOS_PA.RECAUDOSACUMULADOS+ABS(LISEJECUCIONINGRESOS_PA.PORRECAUDAR)+ (V_SALDO_PLAN_PPTAL.EJE_PPT_CREDITO-V_SALDO_PLAN_PPTAL.EJE_PPT_DEBITO+V_SALDO_PLAN_PPTAL.MODIF_INGRESOS_EFECTIVO+V_SALDO_PLAN_PPTAL.MODIF_INGRESOS_PAPELES) <>0
AND V_SALDO_PLAN_PPTAL.MES                                                                                                                                                                                                                                                                                                                                                    = s$mes$s
ORDER BY LISEJECUCIONINGRESOS_PA.COMPANIA,
  LISEJECUCIONINGRESOS_PA.ANO,
  LISEJECUCIONINGRESOS_PA.CUENTA
