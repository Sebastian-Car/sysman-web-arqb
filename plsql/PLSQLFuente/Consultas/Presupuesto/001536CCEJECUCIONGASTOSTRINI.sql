  (RESUMENPPTO_A.APROPIADO +SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO))-(ABS(SUM(SALDO_PLAN_PPTAL.REDUCCION))+SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO)+RESUMENPPTO_A.APLAZAMIENTO) APROPIACIONVIGENTEF,
  RESUMENPPTO_A.APROPIADO APROPIADOF,
  SUBSTR(SALDO_PLAN_PPTAL.CODIGO,0,1) CLASE,
  SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO) CONTRACREDITOF,
  SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO) CREDITOF,
