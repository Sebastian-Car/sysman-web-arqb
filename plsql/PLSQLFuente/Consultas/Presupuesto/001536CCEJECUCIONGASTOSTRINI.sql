  PLAN_PRESUPUESTAL.NOMBRE,
  RESUMENPPTO_A.EJECUCIONPPT-RESUMENPPTO_A.REINTEGRO PAGOSA,
  RESUMENPPTO_P.EJECUCIONPPT-RESUMENPPTO_P.REINTEGRO PAGOSP,
  SUM(SALDO_PLAN_PPTAL.REDUCCION) REDUCCIONF,
