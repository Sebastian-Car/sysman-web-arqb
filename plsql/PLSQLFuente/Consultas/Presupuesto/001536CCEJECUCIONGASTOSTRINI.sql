LEFT JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
ON PLAN_PRESUPUESTAL.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
AND PLAN_PRESUPUESTAL.ANO     = SALDO_PLAN_PPTAL.ANO
AND PLAN_PRESUPUESTAL.ID  = SALDO_PLAN_PPTAL.ID
LEFT JOIN RESUMENPPTO_A
ON PLAN_PRESUPUESTAL.COMPANIA = RESUMENPPTO_A.COMPANIA
AND PLAN_PRESUPUESTAL.ANO     = RESUMENPPTO_A.ANO
AND PLAN_PRESUPUESTAL.ID  = RESUMENPPTO_A.ID)
LEFT JOIN RESUMENPPTO_P
ON RESUMENPPTO_A.COMPANIA     = RESUMENPPTO_P.COMPANIA
AND RESUMENPPTO_A.ANO         = RESUMENPPTO_P.ANO
AND RESUMENPPTO_A.ID      = RESUMENPPTO_P.ID
WHERE SALDO_PLAN_PPTAL.COMPANIA = 's$compania$s'
AND SALDO_PLAN_PPTAL.MES        = s$mes$s
AND SALDO_PLAN_PPTAL.ANO        = s$anio$s
AND PLAN_PRESUPUESTAL.NATURALEZA='D'
AND SALDO_PLAN_PPTAL.ID BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
GROUP BY RESUMENPPTO_A.APROPIADO,
  SUBSTR(SALDO_PLAN_PPTAL.CODIGO,0,1),
  PLAN_PRESUPUESTAL.ID,
  PLAN_PRESUPUESTAL.NOMBRE,
  RESUMENPPTO_A.EJECUCIONPPT-RESUMENPPTO_A.REINTEGRO,
  RESUMENPPTO_P.EJECUCIONPPT-RESUMENPPTO_P.REINTEGRO,
  RESUMENPPTO_P.REG_CONTRACT,
  RESUMENPPTO_A.EJECUCIONPPT,
  RESUMENPPTO_P.REG_CONTRACT+RESUMENPPTO_P.MODIF_REG_CONT +RESUMENPPTO_P.REG_NO_CONTRACT,
  RESUMENPPTO_A.APRDEFINITIVA,
