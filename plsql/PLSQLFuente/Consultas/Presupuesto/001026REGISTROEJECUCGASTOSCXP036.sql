SELECT SUBSTR(V_SALDO_PLAN_PPTAL.CODIGO,1,1) AS CLASE, 
       V_SALDO_PLAN_PPTAL.COMPANIA,
       V_SALDO_PLAN_PPTAL.ANO,
       V_SALDO_PLAN_PPTAL.CODIGO CUENTA,
       V_PLAN_PRESUPUESTAL.NOMBRE,
       V_PLAN_PRESUPUESTAL.DESTINO,
       V_PLAN_PRESUPUESTAL.NIVEL1,
       V_PLAN_PRESUPUESTAL.NIVEL2,
       V_PLAN_PRESUPUESTAL.NIVEL3,
       V_PLAN_PRESUPUESTAL.NIVEL4,
       V_PLAN_PRESUPUESTAL.NIVEL5,
       V_PLAN_PRESUPUESTAL.NIVEL6,
       SUM(CASE WHEN s$miles$s = 1 
              THEN 
                CASE WHEN V_PLAN_PRESUPUESTAL.TIPOVIGENCIA = 'RC' OR V_PLAN_PRESUPUESTAL.TIPOVIGENCIA = 'RA'
                  THEN ROUND((V_SALDO_PLAN_PPTAL.APROPIACION_DEBITO - V_SALDO_PLAN_PPTAL.APROPIACION_CREDITO)+
                        V_SALDO_PLAN_PPTAL.ADICION + (V_SALDO_PLAN_PPTAL.TRASLADO_DEBITO + V_SALDO_PLAN_PPTAL.APLAZAM_DEBITO)+ 
                        V_SALDO_PLAN_PPTAL.REDUCCION - (V_SALDO_PLAN_PPTAL.TRASLADO_CREDITO + V_SALDO_PLAN_PPTAL.APLAZAM_CREDITO)
                        ,3)/1000
                   ELSE 
                      ROUND(V_SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION + V_SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION,3)/1000
                   END
               ELSE 
                CASE WHEN V_PLAN_PRESUPUESTAL.TIPOVIGENCIA = 'RC' OR V_PLAN_PRESUPUESTAL.TIPOVIGENCIA = 'RA'
                  THEN ((V_SALDO_PLAN_PPTAL.APROPIACION_DEBITO - V_SALDO_PLAN_PPTAL.APROPIACION_CREDITO)+
                        V_SALDO_PLAN_PPTAL.ADICION + (V_SALDO_PLAN_PPTAL.TRASLADO_DEBITO + V_SALDO_PLAN_PPTAL.APLAZAM_DEBITO)+ 
                        V_SALDO_PLAN_PPTAL.REDUCCION - (V_SALDO_PLAN_PPTAL.TRASLADO_CREDITO + V_SALDO_PLAN_PPTAL.APLAZAM_CREDITO)
                        )
                   ELSE 
                      V_SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION + V_SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION
                   END
               END)OBLIGACIONESACUM,
        SUM(CASE WHEN s$miles$s = 1
              THEN ROUND(EJE_PPT_DEBITO - EJE_PPT_CREDITO,3)/1000
              ELSE EJE_PPT_DEBITO - EJE_PPT_CREDITO
            END)PAGOSACUM,
        SUM(CASE WHEN s$miles$s = 1 
              THEN 
                (CASE WHEN V_PLAN_PRESUPUESTAL.TIPOVIGENCIA = 'RC' OR V_PLAN_PRESUPUESTAL.TIPOVIGENCIA = 'RA'
                  THEN ROUND((V_SALDO_PLAN_PPTAL.APROPIACION_DEBITO - V_SALDO_PLAN_PPTAL.APROPIACION_CREDITO)+
                        V_SALDO_PLAN_PPTAL.ADICION + (V_SALDO_PLAN_PPTAL.TRASLADO_DEBITO + V_SALDO_PLAN_PPTAL.APLAZAM_DEBITO)+ 
                        V_SALDO_PLAN_PPTAL.REDUCCION - (V_SALDO_PLAN_PPTAL.TRASLADO_CREDITO + V_SALDO_PLAN_PPTAL.APLAZAM_CREDITO)
                        ,3)/1000
                   ELSE 
                      ROUND(V_SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION + V_SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION,3)/1000
                   END
                   )- ROUND(EJE_PPT_DEBITO - EJE_PPT_CREDITO,3)/1000
                ELSE 
                   (CASE WHEN V_PLAN_PRESUPUESTAL.TIPOVIGENCIA = 'RC' OR V_PLAN_PRESUPUESTAL.TIPOVIGENCIA = 'RA'
                  THEN (V_SALDO_PLAN_PPTAL.APROPIACION_DEBITO - V_SALDO_PLAN_PPTAL.APROPIACION_CREDITO)+
                        V_SALDO_PLAN_PPTAL.ADICION + (V_SALDO_PLAN_PPTAL.TRASLADO_DEBITO + V_SALDO_PLAN_PPTAL.APLAZAM_DEBITO)+ 
                        V_SALDO_PLAN_PPTAL.REDUCCION - (V_SALDO_PLAN_PPTAL.TRASLADO_CREDITO + V_SALDO_PLAN_PPTAL.APLAZAM_CREDITO)
                        
                   ELSE 
                      V_SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION + V_SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION
                   END
                   )- (EJE_PPT_DEBITO - EJE_PPT_CREDITO)
               END)TOTALCXP,
          SUM(CASE WHEN V_SALDO_PLAN_PPTAL.MES = s$mes$s
                THEN 
                  CASE WHEN s$miles$s = 1
                    THEN ROUND(EJE_PPT_DEBITO - EJE_PPT_CREDITO,3)/1000
                    ELSE EJE_PPT_DEBITO - EJE_PPT_CREDITO
                  END
              END)PAGOSMES
FROM V_PLAN_PRESUPUESTAL 
  INNER JOIN V_SALDO_PLAN_PPTAL 
    ON   V_PLAN_PRESUPUESTAL.COMPANIA = V_SALDO_PLAN_PPTAL.COMPANIA 
    AND  V_PLAN_PRESUPUESTAL.ANO = V_SALDO_PLAN_PPTAL.ANO 
    AND  V_PLAN_PRESUPUESTAL.ID = V_SALDO_PLAN_PPTAL.ID      
WHERE V_SALDO_PLAN_PPTAL.COMPANIA = s$compania$s
  AND  V_SALDO_PLAN_PPTAL.ANO = s$ano$s
  AND  V_SALDO_PLAN_PPTAL.MES <= s$mes$s 
  AND  V_SALDO_PLAN_PPTAL.CODIGO BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s 
  AND  LENGTH(V_SALDO_PLAN_PPTAL.ID) BETWEEN 0 AND NVL(s$nivel$s,6)
  AND  V_PLAN_PRESUPUESTAL.INFORME NOT IN (0)
  AND  V_PLAN_PRESUPUESTAL.NATURALEZA='D'
GROUP BY SUBSTR(V_SALDO_PLAN_PPTAL.CODIGO,1,1),
  V_SALDO_PLAN_PPTAL.COMPANIA,
  V_SALDO_PLAN_PPTAL.ANO, 
  V_SALDO_PLAN_PPTAL.CODIGO, 
  V_SALDO_PLAN_PPTAL.ID,
  V_PLAN_PRESUPUESTAL.NOMBRE, 
  V_PLAN_PRESUPUESTAL.DESTINO,
  V_PLAN_PRESUPUESTAL.NIVEL1, 
  V_PLAN_PRESUPUESTAL.NIVEL2, 
  V_PLAN_PRESUPUESTAL.NIVEL3,
  V_PLAN_PRESUPUESTAL.NIVEL4,
  V_PLAN_PRESUPUESTAL.NIVEL5,
  V_PLAN_PRESUPUESTAL.NIVEL6
