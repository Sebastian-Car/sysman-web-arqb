SELECT DISTINCT DETALLE_COMPROBANTE_PPTAL.COMPANIA COMPANIAREO,
  DETALLE_COMPROBANTE_PPTAL.ANO ANOREO,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
  DETALLE_COMPROBANTE_PPTAL.CONSECUTIVO,
  DETALLE_COMPROBANTE_PPTAL.FECHA FECHAOBLIGACION,
  DETALLE_COMPROBANTE_PPTAL.ID CUENTAREO,
  DETALLE_COMPROBANTE_PPTAL.CUENTA CODIGO_CUENTA,
  VALOR_DEBITO -VALOR_CREDITO VALOROBLIGACION,
  (VALOR_DEBITO-VALOR_CREDITO)-(DEBITO_AFECTADO-CREDITO_AFECTADO)+(MODIFICACION_DEBITO-MODIFICACION_CREDITO) SALDOOBLIGACION,
  TIPO_COMPROBPP.CLASE,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT TIPOREO,
  DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO NUMEROREO,
  DETALLE_COMPROBANTE_PPTAL.CONSECUTIVOPPTO CONSECUTIVOREO
FROM DETALLE_COMPROBANTE_PPTAL
  LEFT JOIN TIPO_COMPROBPP
     ON DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE          = TIPO_COMPROBPP.CODIGO
    AND DETALLE_COMPROBANTE_PPTAL.COMPANIA         = TIPO_COMPROBPP.COMPANIA
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA        = s$compania$s
     AND DETALLE_COMPROBANTE_PPTAL.ANO                   = $P!{PR_ANORES}
     AND DETALLE_COMPROBANTE_PPTAL.CUENTA BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
     AND TIPO_COMPROBPP.CLASE                      	              = 'REO'
     AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT = '$P!{PR_TIPORES}'
     AND DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO = $P!{PR_NUMERORES}
ORDER BY DETALLE_COMPROBANTE_PPTAL.COMPANIA,
  DETALLE_COMPROBANTE_PPTAL.ANO,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
  DETALLE_COMPROBANTE_PPTAL.CONSECUTIVO
