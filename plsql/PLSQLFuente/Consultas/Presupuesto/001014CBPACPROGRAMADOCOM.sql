SELECT 
DISTINCT
  PACPROGRAMADO.COMPANIA COMPANIACOM,
  PACPROGRAMADO.ANO ANOCOM,
  PACPROGRAMADO.TIPO_CPTE TIPOCOM,
  PACPROGRAMADO.COMPROBANTE NUMEROCOM,
  PACPROGRAMADO.CONSECUTIVO CONSECUTIVOCOM,
  PACPROGRAMADO.CUENTA CUENTACOM,
  PACPROGRAMADO.FECHA,
  MOV_DEBITO-MOV_CREDITO VALORAGIRAR
FROM PACPROGRAMADO
WHERE PACPROGRAMADO.COMPANIA        =  s$compania$s
     AND PACPROGRAMADO.ANO 	       = $P!{PR_ANORES} 
     AND PACPROGRAMADO.TIPO_CPTE        = '$P!{PR_TIPORES}'
     AND PACPROGRAMADO.COMPROBANTE = $P!{PR_NUMERORES}
     AND PACPROGRAMADO.CUENTA BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
