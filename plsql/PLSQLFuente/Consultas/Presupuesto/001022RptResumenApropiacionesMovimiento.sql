WITH RESUMENAPROPIACIONES AS 
 (SELECT PLAN_PRESUPUESTAL.COMPANIA, 
         PLAN_PRESUPUESTAL.ID CUENTA,
         PLAN_PRESUPUESTAL.CODIGO, 
         PLAN_PRESUPUESTAL.NOMBRE, 
         V1.APROPIADO, 
         SUM(SALDO_PLAN_PPTAL.ADICION) ADICION, 
         SUM(SALDO_PLAN_PPTAL.REDUCCION) REDUCCION, 
         SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO - SALDO_PLAN_PPTAL.TRASLADO_CREDITO) TRASLADO, 
         SUM(SALDO_PLAN_PPTAL.APLAZAM_DEBITO - SALDO_PLAN_PPTAL.APLAZAM_CREDITO) APLAZAMIENTO, 
         SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO) CONTRACREDITO, 
         SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO) CREDITO,
         SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD) DISPONIBILIDADACUM, 
         PLAN_PRESUPUESTAL.NIVEL1, 
         PLAN_PRESUPUESTAL.NIVEL2, 
         PLAN_PRESUPUESTAL.NIVEL3, 
         PLAN_PRESUPUESTAL.NIVEL4, 
         PLAN_PRESUPUESTAL.NIVEL5, 
         PLAN_PRESUPUESTAL.NIVEL6, 
         PLAN_PRESUPUESTAL.NIVEL7, 
         PLAN_PRESUPUESTAL.DESTINO, 
         PLAN_PRESUPUESTAL.MOVIMIENTO
  FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL 
        INNER JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL 
             ON PLAN_PRESUPUESTAL.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA  
            AND PLAN_PRESUPUESTAL.ANO      = SALDO_PLAN_PPTAL.ANO  
             AND PLAN_PRESUPUESTAL.ID      = SALDO_PLAN_PPTAL.ID

         LEFT JOIN (SELECT SALDO.APROPIACION_DEBITO - SALDO.APROPIACION_CREDITO APROPIADO,
         SALDO.MES,
         SALDO.ID,
         SALDO.ANO,
         SALDO.COMPANIA
                    FROM V_SALDO_PLAN_PPTAL SALDO)  V1 
                 ON V1.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
                AND V1.ANO = PLAN_PRESUPUESTAL.ANO
                AND V1.ID = PLAN_PRESUPUESTAL.ID
                AND V1.MES = 0


  WHERE SALDO_PLAN_PPTAL.COMPANIA         = s$compania$s 
          AND SALDO_PLAN_PPTAL.ID BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s' 
          AND SALDO_PLAN_PPTAL.ANO        = s$anio$s 
          AND SALDO_PLAN_PPTAL.MES BETWEEN 0 AND s$mes$s
          AND PLAN_PRESUPUESTAL.MOVIMIENTO NOT IN (0)
          AND PLAN_PRESUPUESTAL.NATURALEZA = 'D'
  GROUP BY PLAN_PRESUPUESTAL.COMPANIA, 
           PLAN_PRESUPUESTAL.CODIGO, 
           PLAN_PRESUPUESTAL.NOMBRE, 
           V1.APROPIADO,
           SALDO_PLAN_PPTAL.CODIGO, 
           PLAN_PRESUPUESTAL.NIVEL1, 
           PLAN_PRESUPUESTAL.NIVEL2,
           PLAN_PRESUPUESTAL.NIVEL3,
           PLAN_PRESUPUESTAL.NIVEL4,
           PLAN_PRESUPUESTAL.NIVEL5,
           PLAN_PRESUPUESTAL.NIVEL6,
           PLAN_PRESUPUESTAL.NIVEL7,
           PLAN_PRESUPUESTAL.DESTINO, 
           PLAN_PRESUPUESTAL.MOVIMIENTO, 
           SALDO_PLAN_PPTAL.COMPANIA, 
           SALDO_PLAN_PPTAL.ANO, 
           PLAN_PRESUPUESTAL.NATURALEZA,
           PLAN_PRESUPUESTAL.ANO,
           PLAN_PRESUPUESTAL.ID
  HAVING SUM(SALDO_PLAN_PPTAL.APROPIACION_DEBITO - SALDO_PLAN_PPTAL.APROPIACION_CREDITO) NOT IN (0) 
      OR SUM(SALDO_PLAN_PPTAL.ADICION)          NOT IN (0) 
      OR SUM(SALDO_PLAN_PPTAL.REDUCCION)        NOT IN (0)
      OR SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO) NOT IN (0)
      OR SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO)  NOT IN (0)
  ORDER BY SALDO_PLAN_PPTAL.COMPANIA, SALDO_PLAN_PPTAL.ANO
 )  
  SELECT COMPANIA, 
         CUENTA, 
         CODIGO, 
         NOMBRE, 
         APROPIADO, 
         ADICION, 
         REDUCCION, 
         CREDITO, 
         CONTRACREDITO, 
         DESTINO, 
         NIVEL1, 
         NIVEL2, 
         NIVEL3, 
         NIVEL4, 
         NIVEL5, 
         NIVEL6, 
         NIVEL7, 
         DISPONIBILIDADACUM,
         TRASLADO,
         APLAZAMIENTO,
         (APROPIADO + ADICION + REDUCCION + TRASLADO + APLAZAMIENTO) TOTALAPROPIADO,
         DISPONIBILIDADACUM DISPONIBILIDADES,
         (ADICION + REDUCCION + TRASLADO +APLAZAMIENTO) AS MODIFICACIONES,
         ((APROPIADO + ADICION + REDUCCION + TRASLADO + APLAZAMIENTO) - DISPONIBILIDADACUM) SALDOXCOMPROMETER,
         CASE WHEN (APROPIADO + ADICION + REDUCCION + TRASLADO + APLAZAMIENTO) = 0
              THEN 0
              ELSE ROUND((DISPONIBILIDADACUM/(APROPIADO + ADICION + REDUCCION + TRASLADO + APLAZAMIENTO))*100,2)
         END PORC_EJECUTADO,
         CASE WHEN (APROPIADO + ADICION + REDUCCION + TRASLADO + APLAZAMIENTO) = 0
              THEN 0
              ELSE ROUND((((APROPIADO + ADICION + REDUCCION + TRASLADO + APLAZAMIENTO) - DISPONIBILIDADACUM)/(APROPIADO + ADICION + REDUCCION + TRASLADO + APLAZAMIENTO))*100,2)
         END PORC_POREJECUTAR
   FROM RESUMENAPROPIACIONES
  ORDER BY CUENTA
