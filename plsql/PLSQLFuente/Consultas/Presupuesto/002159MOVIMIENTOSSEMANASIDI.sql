SELECT  
    PLAN_PRESUPUESTAL.COMPANIA,
    PLAN_PRESUPUESTAL.ANO,
    PLAN_PRESUPUESTAL.CODIGO,
    PLAN_PRESUPUESTAL.ID CODIGOFUENTE,
    PLAN_PRESUPUESTAL.NOMBRE, 
    PLAN_PRESUPUESTAL.APROPIACIONINICIAL APROPIACION_VIGENTE,
    PLAN_PRESUPUESTAL.APROPIACIONINICIAL +
    SUM(CASE WHEN TIPO_COMPROBPP.CLASE IN ('ADC','RED','TRA','APL')               
             THEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO
             ELSE 0 
             END) APROPIACION_DISPONIBLE,             
    SUM(CASE WHEN TIPO_COMPROBPP.CLASE IN ('DIS','ADD','DMD') 
              AND DETALLE_COMPROBANTE_PPTAL.FECHA >= TO_DATE('s$fechaPar$s','DD/MM/YYYY')
             THEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO
             ELSE 0 
             END) DISPONIBILIDAD_SEMANA,
    SUM(CASE WHEN TIPO_COMPROBPP.CLASE IN ('RES','ADR','DMR') 
              AND DETALLE_COMPROBANTE_PPTAL.FECHA >= TO_DATE('s$fechaPar$s','DD/MM/YYYY')
             THEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO
             ELSE 0 
             END) RES_SEMANA,
    SUM(CASE WHEN TIPO_COMPROBPP.CLASE IN ('REO','DRO','ARO','MRO') 
              AND DETALLE_COMPROBANTE_PPTAL.FECHA >= TO_DATE('s$fechaPar$s','DD/MM/YYYY')
             THEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO
             ELSE 0 
             END) REO_SEMANA,
     0 AS SUSPENSION_ACUM,
     SUM(CASE WHEN TIPO_COMPROBPP.CLASE IN ('DIS','ADD','DMD') 
             THEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO
             ELSE 0 
             END) DISPONIBILIDAD_ACUM,
    SUM(CASE WHEN TIPO_COMPROBPP.CLASE IN ('RES','ADR','DMR') 
             THEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO
             ELSE 0 
             END) RES_ACUM,
    SUM(CASE WHEN TIPO_COMPROBPP.CLASE IN ('REO','DRO','ARO','MRO')
             THEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO
             ELSE 0 
             END) REO_ACUM
FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL 
INNER JOIN DETALLE_COMPROBANTE_PPTAL  
    ON PLAN_PRESUPUESTAL.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA
   AND PLAN_PRESUPUESTAL.ANO      = DETALLE_COMPROBANTE_PPTAL.ANO 
   AND PLAN_PRESUPUESTAL.ID       = DETALLE_COMPROBANTE_PPTAL.ID 
 INNER JOIN TIPO_COMPROBPP
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA  = TIPO_COMPROBPP.COMPANIA
   AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
 INNER JOIN FUENTE_RECURSOS
    ON FUENTE_RECURSOS.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
   AND FUENTE_RECURSOS.ANO      = PLAN_PRESUPUESTAL.ANO
   AND FUENTE_RECURSOS.CODIGO   = PLAN_PRESUPUESTAL.FUENTE_RECURSO  
WHERE PLAN_PRESUPUESTAL.COMPANIA = s$compania$s
  AND PLAN_PRESUPUESTAL.ANO = s$anio$s
  AND DETALLE_COMPROBANTE_PPTAL.FECHA BETWEEN TO_DATE('s$primeroDeAno$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY') 
  AND PLAN_PRESUPUESTAL.ID BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
GROUP BY PLAN_PRESUPUESTAL.COMPANIA, 
         PLAN_PRESUPUESTAL.ANO, 
         PLAN_PRESUPUESTAL.CODIGO,
         PLAN_PRESUPUESTAL.ID,
         PLAN_PRESUPUESTAL.APROPIACIONINICIAL,
         PLAN_PRESUPUESTAL.NOMBRE
ORDER BY PLAN_PRESUPUESTAL.CODIGO, PLAN_PRESUPUESTAL.ID