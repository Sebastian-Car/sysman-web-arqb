SELECT COMPROBANTE_PPTAL.COMPANIA,
  COMPROBANTE_PPTAL.TIPO,
  COMPROBANTE_PPTAL.DEPENDENCIA,
  COMPROBANTE_PPTAL.NUMERO,
  COMPROBANTE_PPTAL.FECHA,
  COMPROBANTE_PPTALAFECTADOS.TIPO_CPTE_AFECT,
  COMPROBANTE_PPTALAFECTADOS.COMPROBANTE_AFECT CMPTE_AFECTADO,
  COMPROBANTE_PPTAL.DESCRIPCION,
  CASE WHEN COMPROBANTE_PPTAL.DEBITO =0 THEN COMPROBANTE_PPTAL.CREDITO ELSE COMPROBANTE_PPTAL.DEBITO END AS VALORUNICO,
  TERCERO.NOMBRE AS NOMBRETERCERO,
  TIPO_COMPROBPP.NOMBRE,
  COMPROBANTE_PPTAL.TERCERO AS CODTERCERO,
  CASE WHEN COMPROBANTE_PPTAL.SUCURSAL='s$sucursal$s' THEN ' ' ELSE COMPROBANTE_PPTAL.SUCURSAL END AS CODSUCURSAL,
  COMPROBANTE_PPTAL.DEBITO_AFECTADO
FROM (COMPROBANTE_PPTAL
LEFT JOIN COMPROBANTE_PPTALAFECTADOS ON
COMPROBANTE_PPTALAFECTADOS.COMPANIA = COMPROBANTE_PPTAL.COMPANIA AND
COMPROBANTE_PPTALAFECTADOS.ANO = COMPROBANTE_PPTAL.ANO AND
COMPROBANTE_PPTALAFECTADOS.TIPO_CPTE =COMPROBANTE_PPTAL.TIPO AND 
COMPROBANTE_PPTALAFECTADOS.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO
INNER JOIN TERCERO ON
COMPROBANTE_PPTAL.COMPANIA = TERCERO.COMPANIA AND
COMPROBANTE_PPTAL.TERCERO   = TERCERO.NIT AND
COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL)
INNER JOIN TIPO_COMPROBPP ON
COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA AND
COMPROBANTE_PPTAL.TIPO = TIPO_COMPROBPP.CODIGO
WHERE COMPROBANTE_PPTAL.COMPANIA = s$compania$s
  AND FECHA
    BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND COMPROBANTE_PPTAL.TIPO
    BETWEEN 's$tipoInicial$s' AND 's$tipoFinal$s'
  AND COMPROBANTE_PPTAL.NUMERO
    BETWEEN s$numeroInicial$s AND s$numeroFinal$s
