SELECT 
s$aplicarMiles$sVS.REG_CONTRACT+VS.REG_NO_CONTRACT+VS.REG_REVERSION+VS.MODIF_REG_CONT+VS.MODIF_REG_NOCONTs$dividir$s AS COMPROMISOSMES,
s$aplicarMiles$sVS.REGISTRO_OBLIGACION+VS.MODIF_REGISTRO_OBLIGACIONs$dividir$s  AS OBLIGACIONESMES,
s$aplicarMiles$sVS.EJE_PPT_DEBITO-VS.EJE_PPT_CREDITOs$dividir$s AS PAGOSMES,
REGISTROEJECUCGASTOS_036.CLASE,
REGISTROEJECUCGASTOS_036.CUENTA,
REGISTROEJECUCGASTOS_036.DESTINO,
REGISTROEJECUCGASTOS_036.NIVEL1,
REGISTROEJECUCGASTOS_036.NIVEL2,
REGISTROEJECUCGASTOS_036.NIVEL3,
REGISTROEJECUCGASTOS_036.NIVEL4,
REGISTROEJECUCGASTOS_036.NIVEL5,
REGISTROEJECUCGASTOS_036.NIVEL6,
REGISTROEJECUCGASTOS_036.NOMBRE,
REGISTROEJECUCGASTOS_036.APROPIACIONVIGENTE,  
REGISTROEJECUCGASTOS_036.COMPROMISOSACUM,
REGISTROEJECUCGASTOS_036.OBLIGACIONESACUM, 
REGISTROEJECUCGASTOS_036.PAGOSACUM  
FROM (SELECT SUBSTR(SALDO_PLAN_PPTAL.ID, 1, 1) CLASE, 
         SALDO_PLAN_PPTAL.COMPANIA,  
         SALDO_PLAN_PPTAL.ANO,  
         SALDO_PLAN_PPTAL.ID CUENTA,
         PLAN_PRESUPUESTAL.DESTINO,
         PLAN_PRESUPUESTAL.NIVEL1,
         PLAN_PRESUPUESTAL.NIVEL2,
         PLAN_PRESUPUESTAL.NIVEL3,
         PLAN_PRESUPUESTAL.NIVEL4,
         PLAN_PRESUPUESTAL.NIVEL5,
         PLAN_PRESUPUESTAL.NIVEL6,
         PLAN_PRESUPUESTAL.NOMBRE,
         s$aplicarMiles$s(SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' 
                  THEN SALDO_PLAN_PPTAL.APROPIACION_DEBITO-SALDO_PLAN_PPTAL.APROPIACION_CREDITO 
                  ELSE SALDO_PLAN_PPTAL.APROPIACION_CREDITO-SALDO_PLAN_PPTAL.APROPIACION_DEBITO END)+
                  SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO)+
                  SUM(SALDO_PLAN_PPTAL.REDUCCION)+SUM(SALDO_PLAN_PPTAL.APLAZAM_DEBITO -SALDO_PLAN_PPTAL.APLAZAM_CREDITO))-
                  (SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO))s$dividir$s AS APROPIACIONVIGENTE,
         s$aplicarMiles$sSUM(SALDO_PLAN_PPTAL.REG_CONTRACT)+SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)+SUM(SALDO_PLAN_PPTAL.REG_REVERSION)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT)s$dividir$s AS COMPROMISOSACUM,
         s$aplicarMiles$sSUM(SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION)+SUM(SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION)s$dividir$s AS OBLIGACIONESACUM, 
         s$aplicarMiles$sSUM(SALDO_PLAN_PPTAL.EJE_PPT_DEBITO-SALDO_PLAN_PPTAL.EJE_PPT_CREDITO)s$dividir$s AS PAGOSACUM  
 FROM V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL 
  INNER JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL 
     ON SALDO_PLAN_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA 
    AND SALDO_PLAN_PPTAL.ANO      = PLAN_PRESUPUESTAL.ANO  
    AND SALDO_PLAN_PPTAL.ID       = PLAN_PRESUPUESTAL.ID    
  WHERE SALDO_PLAN_PPTAL.COMPANIA = s$compania$s
    AND SALDO_PLAN_PPTAL.MES BETWEEN 0 AND s$mes$s
    AND SALDO_PLAN_PPTAL.ANO = s$anio$s
    AND SALDO_PLAN_PPTAL.CODIGO BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
    AND LENGTH(SALDO_PLAN_PPTAL.ID) <= s$nivelCuenta$s
    AND PLAN_PRESUPUESTAL.INFORME <> 0
    AND PLAN_PRESUPUESTAL.NATURALEZA = 'D'
  GROUP BY SUBSTR(SALDO_PLAN_PPTAL.ID, 1, 1), 
           SALDO_PLAN_PPTAL.COMPANIA, 
           SALDO_PLAN_PPTAL.ANO,
           SALDO_PLAN_PPTAL.ID,  
           PLAN_PRESUPUESTAL.DESTINO,
           PLAN_PRESUPUESTAL.NIVEL1, 
           PLAN_PRESUPUESTAL.NIVEL2,
           PLAN_PRESUPUESTAL.NIVEL3,
           PLAN_PRESUPUESTAL.NIVEL4,
           PLAN_PRESUPUESTAL.NIVEL5,
           PLAN_PRESUPUESTAL.NIVEL6, 
           PLAN_PRESUPUESTAL.NOMBRE ) REGISTROEJECUCGASTOS_036
INNER JOIN V_SALDO_PLAN_PPTAL VS
ON (REGISTROEJECUCGASTOS_036.COMPANIA = VS.COMPANIA)
AND (REGISTROEJECUCGASTOS_036.ANO     = VS.ANO)
AND (REGISTROEJECUCGASTOS_036.CUENTA  = VS.ID)
WHERE VS.MES = s$mes$s
ORDER BY REGISTROEJECUCGASTOS_036.CUENTA 
