SELECT 	REGISTROEJECUCGASTOS_036_AIM.CLASE,
        REGISTROEJECUCGASTOS_036_AIM.ANO,
        REGISTROEJECUCGASTOS_036_AIM.CUENTA,
        REGISTROEJECUCGASTOS_036_AIM.NOMBRE,
        REGISTROEJECUCGASTOS_036_AIM.APROPIACIONVIGENTE,
        CASE WHEN 1 = s$miles$s
          THEN ROUND(	SALDO_PLAN_PPTAL.REG_CONTRACT + SALDO_PLAN_PPTAL.REG_NO_CONTRACT  +
                      SALDO_PLAN_PPTAL.REG_REVERSION + SALDO_PLAN_PPTAL.MODIF_REG_CONT +
                      SALDO_PLAN_PPTAL.MODIF_REG_NOCONT,-3)/1000
          ELSE (SALDO_PLAN_PPTAL.REG_CONTRACT + SALDO_PLAN_PPTAL.REG_NO_CONTRACT  +
                SALDO_PLAN_PPTAL.REG_REVERSION + SALDO_PLAN_PPTAL.MODIF_REG_CONT +
                SALDO_PLAN_PPTAL.MODIF_REG_NOCONT)
        END	AS COMPROMISOSMES,
        REGISTROEJECUCGASTOS_036_AIM.COMPROMISOSACUM,
        CASE WHEN 1 = s$miles$s
          THEN ROUND(REGISTRO_OBLIGACION + MODIF_REGISTRO_OBLIGACION,-3)/1000
          ELSE (REGISTRO_OBLIGACION + MODIF_REGISTRO_OBLIGACION)
        END AS OBLIGACIONESMES,
        REGISTROEJECUCGASTOS_036_AIM.OBLIGACIONESACUM,
        CASE WHEN 1 = s$miles$s
          THEN ROUND((EJE_PPT_DEBITO - EJE_PPT_CREDITO),-3)/1000
          ELSE (EJE_PPT_DEBITO - EJE_PPT_CREDITO)
        END	AS PAGOSMES,
        REGISTROEJECUCGASTOS_036_AIM.PAGOSACUM
FROM
        (SELECT SUBSTR(SALDO_PLAN_PPTAL.CODIGO,1,1) AS CLASE,
            SALDO_PLAN_PPTAL.COMPANIA,
            SALDO_PLAN_PPTAL.ANO,
            SALDO_PLAN_PPTAL.ID CUENTA,
            PLAN_PRESUPUESTAL.NOMBRE,
            CASE WHEN 1 = s$miles$s
              THEN ROUND(SUM(((APROPIACION_DEBITO-APROPIACION_CREDITO) + 
                      SALDO_PLAN_PPTAL.ADICION + SALDO_PLAN_PPTAL.TRASLADO_DEBITO + 
                      SALDO_PLAN_PPTAL.REDUCCION + (APLAZAM_DEBITO-APLAZAM_CREDITO))-
                      (SALDO_PLAN_PPTAL.TRASLADO_CREDITO)),-3)/1000
              ELSE SUM(((APROPIACION_DEBITO-APROPIACION_CREDITO) + 
                      SALDO_PLAN_PPTAL.ADICION + SALDO_PLAN_PPTAL.TRASLADO_DEBITO + 
                      SALDO_PLAN_PPTAL.REDUCCION + (APLAZAM_DEBITO-APLAZAM_CREDITO))-
                      (SALDO_PLAN_PPTAL.TRASLADO_CREDITO))
            END	AS APROPIACIONVIGENTE,
            CASE WHEN 1 = s$miles$s 
              THEN ROUND(	SUM(SALDO_PLAN_PPTAL.REG_CONTRACT + SALDO_PLAN_PPTAL.REG_NO_CONTRACT + 
                      SALDO_PLAN_PPTAL.REG_REVERSION + SALDO_PLAN_PPTAL.MODIF_REG_CONT + 
                      SALDO_PLAN_PPTAL.MODIF_REG_NOCONT),-3)/1000
              ELSE SUM(SALDO_PLAN_PPTAL.REG_CONTRACT + SALDO_PLAN_PPTAL.REG_NO_CONTRACT + 
                      SALDO_PLAN_PPTAL.REG_REVERSION + SALDO_PLAN_PPTAL.MODIF_REG_CONT + 
                      SALDO_PLAN_PPTAL.MODIF_REG_NOCONT)
            END	AS COMPROMISOSACUM,
            CASE WHEN 1 = s$miles$s
              THEN ROUND(SUM(SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION + SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION),-3)/1000
              ELSE SUM(SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION + SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION)
            END	AS OBLIGACIONESACUM,
            CASE WHEN 1 = s$miles$s
              THEN ROUND(SUM(EJE_PPT_DEBITO - EJE_PPT_CREDITO),-3)/1000
              ELSE SUM(EJE_PPT_DEBITO - EJE_PPT_CREDITO)
            END	AS PAGOSACUM,
            PLAN_PRESUPUESTAL.MOVIMIENTO,
            PLAN_PRESUPUESTAL.DESTINO,
            PLAN_PRESUPUESTAL.NIVEL1,
            PLAN_PRESUPUESTAL.NIVEL2,
            PLAN_PRESUPUESTAL.NIVEL3,
            PLAN_PRESUPUESTAL.NIVEL4,
            PLAN_PRESUPUESTAL.NIVEL5,
            PLAN_PRESUPUESTAL.NIVEL6,
            PLAN_PRESUPUESTAL.NIVEL7
      FROM V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
        LEFT JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
          ON SALDO_PLAN_PPTAL.COMPANIA      = PLAN_PRESUPUESTAL.COMPANIA
          AND SALDO_PLAN_PPTAL.ANO          = PLAN_PRESUPUESTAL.ANO
          AND SALDO_PLAN_PPTAL.ID           = PLAN_PRESUPUESTAL.ID
      WHERE SALDO_PLAN_PPTAL.COMPANIA = s$compania$s
        AND SALDO_PLAN_PPTAL.ANO = s$ano$s
        AND SALDO_PLAN_PPTAL.MES BETWEEN 0 AND s$mes$s
        AND SALDO_PLAN_PPTAL.ID BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
        AND LENGTH(SALDO_PLAN_PPTAL.ID) BETWEEN 0 AND NVL(s$nivel$s,6)
        AND PLAN_PRESUPUESTAL.INFORME NOT IN(0)
        AND PLAN_PRESUPUESTAL.NATURALEZA ='D'
      GROUP BY 	SALDO_PLAN_PPTAL.COMPANIA,
            SALDO_PLAN_PPTAL.ANO,
            SALDO_PLAN_PPTAL.CODIGO,
            SALDO_PLAN_PPTAL.ID,
            PLAN_PRESUPUESTAL.NOMBRE,
            PLAN_PRESUPUESTAL.MOVIMIENTO,
            PLAN_PRESUPUESTAL.DESTINO,
            PLAN_PRESUPUESTAL.NIVEL1,
            PLAN_PRESUPUESTAL.NIVEL2,
            PLAN_PRESUPUESTAL.NIVEL3,
            PLAN_PRESUPUESTAL.NIVEL4,
            PLAN_PRESUPUESTAL.NIVEL5,
            PLAN_PRESUPUESTAL.NIVEL6,
            PLAN_PRESUPUESTAL.NIVEL7,
            PLAN_PRESUPUESTAL.NATURALEZA
  )REGISTROEJECUCGASTOS_036_AIM
	LEFT JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
		ON REGISTROEJECUCGASTOS_036_AIM.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA 
		AND REGISTROEJECUCGASTOS_036_AIM.ANO      = SALDO_PLAN_PPTAL.ANO
		AND REGISTROEJECUCGASTOS_036_AIM.CUENTA    = SALDO_PLAN_PPTAL.ID
WHERE SALDO_PLAN_PPTAL.MES = s$mes$s
ORDER BY 	REGISTROEJECUCGASTOS_036_AIM.COMPANIA,
			REGISTROEJECUCGASTOS_036_AIM.ANO,
			REGISTROEJECUCGASTOS_036_AIM.CUENTA
