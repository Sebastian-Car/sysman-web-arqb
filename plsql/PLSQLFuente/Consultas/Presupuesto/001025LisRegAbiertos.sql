SELECT DETALLE_COMPROBANTE_PPTAL.COMPANIA,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
  COMPROBANTE_PPTAL.TIPOCONTRATO
  || ' - '
  || COMPROBANTE_PPTAL.NUMEROCONTRATO 							CONTRATO,
  DETALLE_COMPROBANTE_PPTAL.DESCRIPCION,
  (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)-(DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO)+(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO) AS SALDO,
  TIPO_COMPROBPP.NOMBRE TIPO_COMPROBPP_NOMBRE,
  (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO       -DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)        		MOVIMIENTO,
  (DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO    -DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO)     		AFECTACION,
  (DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO) 	MODIFICACION,
  PLAN_PRESUPUESTAL.NOMBRE PLAN_PRESUPUESTAL_NOMBRE,
  DETALLE_COMPROBANTE_PPTAL.TERCERO  AS CODTERCERO,
  DETALLE_COMPROBANTE_PPTAL.SUCURSAL  AS CODSUCURSAL,
  TERCERO.NOMBRE                     								NOMBRETERCERO,
  DETALLE_COMPROBANTE_PPTAL.FECHA,
  CASE WHEN s$tipoCuenta$s=1 THEN DETALLE_COMPROBANTE_PPTAL.CUENTA ELSE RPAD(NVL(PLAN_PRESUPUESTAL.NIVEL1,''),3,' ')
  || '-'
  || RPAD(NVL(PLAN_PRESUPUESTAL.NIVEL2,''),3,' ')
  || '-'
  || RPAD(NVL(PLAN_PRESUPUESTAL.NIVEL3,''),3,' ')
  || '-'
  || RPAD(NVL(PLAN_PRESUPUESTAL.NIVEL4,''),3,' ')
  || '-'
  || RPAD(NVL(PLAN_PRESUPUESTAL.NIVEL5,''),3,' ')
  || '-'
  || RPAD(NVL(PLAN_PRESUPUESTAL.NIVEL6,''),3,' ')
  || '-'
  || RPAD(NVL(PLAN_PRESUPUESTAL.NIVEL7,''),3,' ') END                         					RUBRO,
  CASE WHEN s$tipoCuenta$s=1 THEN 'CÓDIGO CUENTA' ELSE 'Fid-Une-Prog-Spr-Proy-Dis-Rec' END 			TITULOCUENTA
FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
INNER JOIN DETALLE_COMPROBANTE_PPTAL ON
PLAN_PRESUPUESTAL.COMPANIA  = DETALLE_COMPROBANTE_PPTAL.COMPANIA AND
PLAN_PRESUPUESTAL.ANO       = DETALLE_COMPROBANTE_PPTAL.ANO AND
PLAN_PRESUPUESTAL.ID        = DETALLE_COMPROBANTE_PPTAL.ID
INNER JOIN TIPO_COMPROBPP ON
TIPO_COMPROBPP.COMPANIA     = DETALLE_COMPROBANTE_PPTAL.COMPANIA AND
TIPO_COMPROBPP.CODIGO       = DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE
INNER JOIN TERCERO ON
DETALLE_COMPROBANTE_PPTAL.COMPANIA = TERCERO.COMPANIA AND
DETALLE_COMPROBANTE_PPTAL.TERCERO  = TERCERO.NIT AND
DETALLE_COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL
INNER JOIN COMPROBANTE_PPTAL ON 
DETALLE_COMPROBANTE_PPTAL.COMPANIA     = COMPROBANTE_PPTAL.COMPANIA AND
DETALLE_COMPROBANTE_PPTAL.ANO          = COMPROBANTE_PPTAL.ANO AND
DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE    = COMPROBANTE_PPTAL.TIPO AND
DETALLE_COMPROBANTE_PPTAL.COMPROBANTE  = COMPROBANTE_PPTAL.NUMERO
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA = s$compania$s
AND TIPO_COMPROBPP.CLASE                 = 'RES'
AND   TRUNC(DETALLE_COMPROBANTE_PPTAL.FECHA) BETWEEN  TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY') 
AND ROUND((DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)-(DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO)+(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO),2)<>0
ORDER BY DETALLE_COMPROBANTE_PPTAL.COMPANIA,
  DETALLE_COMPROBANTE_PPTAL.ANO,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE
