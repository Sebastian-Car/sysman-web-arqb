SELECT
  COMPROBANTE_PPTAL.COMPANIA COMPANIARES,
  COMPROBANTE_PPTAL.ANO ANORES,
  COMPROBANTE_PPTAL.TIPO TIPORES,
  COMPROBANTE_PPTAL.NUMERO NUMERORES,
  DETALLE_COMPROBANTE_PPTAL.CONSECUTIVO CONSECUTIVORES,
  COMPROBANTE_PPTAL.COMPANIA||COMPROBANTE_PPTAL.ANO||COMPROBANTE_PPTAL.TIPO||COMPROBANTE_PPTAL.NUMERO||DETALLE_COMPROBANTE_PPTAL.CUENTA AGRUPAR, 
  COMPROBANTE_PPTAL.FECHA FECHACOMPROMISO,
  DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO NUMERODISP,
  TERCERO.NOMBRE BENEFICIARIO,
  COMPROBANTE_PPTAL.DESCRIPCION OBJETO,
  DETALLE_COMPROBANTE_PPTAL.ID CUENTARES,
  DETALLE_COMPROBANTE_PPTAL.CUENTA CODIGO_CUENTA,
  VALOR_DEBITO-VALOR_CREDITO TOTALCOMPROMISO,
  V_PLAN_PRESUPUESTAL.NOMBRE,
  V_PLAN_PRESUPUESTAL.NIVEL1,
  V_PLAN_PRESUPUESTAL.NIVEL2,
  V_PLAN_PRESUPUESTAL.NIVEL3,
  V_PLAN_PRESUPUESTAL.NIVEL4,
  V_PLAN_PRESUPUESTAL.NIVEL5,
  V_PLAN_PRESUPUESTAL.NIVEL6, 
  V_PLAN_PRESUPUESTAL.NIVEL7
FROM COMPROBANTE_PPTAL  
  LEFT JOIN TERCERO 
     ON COMPROBANTE_PPTAL.COMPANIA = TERCERO.COMPANIA
    AND COMPROBANTE_PPTAL.TERCERO = TERCERO.NIT
    AND COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL
  INNER JOIN DETALLE_COMPROBANTE_PPTAL
     ON COMPROBANTE_PPTAL.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA
    AND COMPROBANTE_PPTAL.ANO = DETALLE_COMPROBANTE_PPTAL.ANO
    AND COMPROBANTE_PPTAL.TIPO = DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE
    AND COMPROBANTE_PPTAL.NUMERO = DETALLE_COMPROBANTE_PPTAL.COMPROBANTE
  INNER JOIN V_PLAN_PRESUPUESTAL 
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = V_PLAN_PRESUPUESTAL.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO = V_PLAN_PRESUPUESTAL.ANO
    AND DETALLE_COMPROBANTE_PPTAL.ID = V_PLAN_PRESUPUESTAL.ID
WHERE COMPROBANTE_PPTAL.COMPANIA = s$compania$s
  AND COMPROBANTE_PPTAL.ANO = $P!{PR_ANORES}
  AND COMPROBANTE_PPTAL.TIPO = '$P!{PR_TIPORES}'
  AND COMPROBANTE_PPTAL.NUMERO = $P!{PR_NUMERORES}
  AND DETALLE_COMPROBANTE_PPTAL.ID BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
ORDER BY DETALLE_COMPROBANTE_PPTAL.COMPANIA,
  DETALLE_COMPROBANTE_PPTAL.ANO,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
  DETALLE_COMPROBANTE_PPTAL.CONSECUTIVO
