MERGE INTO CONSULTAS FIN USING (SELECT '002530INFORMEPORMETAS' INFORME ,TO_CLOB(q'[SELECT
    CLASE_COM.CODIGO || '-'  || CLASE_COM.DESCRIPCION AS TIPO_COMPROBANTE,
    COM.TIPOCONTRATO,
    COM.NUMEROCONTRATO,
    COM.TIPO_CPTE_AFECT,
    DET.CMPTE_AFECTADO   AS DISPONIBILIDAD,
    DET.TIPO_CPTE,
    DET.COMPROBANTE,
    SUM(CASE
        WHEN TIPO_COM.CLASE  IN ('RES','ADR') THEN
            DET.VALOR_DEBITO
        ELSE
            - 1 * (DET.VALOR_CREDITO)
    END )AS VALOR,
    TO_CHAR(DET.FECHA, 'DD/MM/YYYY') FECHA,
    T.NOMBRE,
    DET.TERCERO,
    DET.DESCRIPCION      AS OBJETO,
    DET.CUENTA           AS RUBRO,
    PLAN.NOMBRE          AS ACTIVIDAD,
    DET.MES,
    DET.ANO,
    DET.FUENTE_RECURSO   || '-'   || FT.NOMBRE FUENTE_RECURSO,
    COM.DEPENDENCIA  || '-'   || DEPENDENCIA.NOMBRE DEPENDENCIA ,
    DET.SUBPROGRAMA      META,
    TIPO.NOMBRE          NOMBRE_META
FROM
    DETALLE_COMPROBANTE_PPTAL   DET
    INNER JOIN COMPROBANTE_PPTAL           COM ON DET.COMPANIA = COM.COMPANIA
                                        AND DET.ANO = COM.ANO
                                        AND DET.TIPO_CPTE = COM.TIPO
                                        AND DET.COMPROBANTE = COM.NUMERO
    INNER JOIN FUENTE_RECURSOS             FT ON DET.COMPANIA = FT.COMPANIA
                                     AND DET.ANO = FT.ANO
                                     AND DET.FUENTE_RECURSO = FT.CODIGO
    INNER JOIN TIPO_COMPROBPP              TIPO_COM ON COM.COMPANIA = TIPO_COM.COMPANIA
                                          AND COM.TIPO = TIPO_COM.CODIGO
    INNER JOIN CLASECNTPRES                CLASE_COM ON TIPO_COM.CLASE = CLASE_COM.CODIGO
    INNER JOIN TERCERO                     T ON DET.COMPANIA = T.COMPANIA
                            AND DET.TERCERO = T.NIT
                            AND DET.SUCURSAL = T.SUCURSAL
    INNER JOIN PLAN_PRESUPUESTAL           PLAN ON DET.COMPANIA = PLAN.COMPANIA
                                         AND DET.ANO = PLAN.ANO
                                         AND DET.CUENTA = PLAN.CODIGO
     LEFT JOIN DEPENDENCIA 
     ON COM.COMPANIA=DEPENDENCIA.COMPANIA
     AND COM.DEPENDENCIA=DEPENDENCIA.CODIGO
    
    LEFT JOIN TIPOCLASIFICADOR            TIPO ON DET.COMPANIA = TIPO.COMPANIA
                                       AND DET.ANO = TIPO.ANO
                                       AND '003'= TIPO.CLASECLASIFICADOR
                                       AND DET.SUBPROGRAMA = TIPO.CODIGO
WHERE
    DET.COMPANIA = s$compania$s
    AND TIPO_COM.CLASE IN ('RES','DMR','ADR')
    AND DET.ANO = s$ano$s
    AND DET.SUBPROGRAMA BETWEEN 's$metaInicial$s' AND 's$metaFinal$s'
    AND DET.MES BETWEEN 's$mesInicial$s' AND 's$mesFinal$s'
GROUP BY
    CLASE_COM.CODIGO,
    CLASE_COM.CODIGO    || '-'    || CLASE_COM.DESCRIPCION,
    CLASE_COM.DESCRIPCION,
    COM.TIPOCONTRATO,
    COM.NUMEROCONTRATO,
    COM.TIPO_CPTE_AFECT,
    DET.CMPTE_AFECTADO,
    DET.TIPO_CPTE,
    DET.COMPROBANTE,
    DET.FECHA,
    T.NOMBRE,
    DET.TERCERO,
    DET.DESCRIPCION,
    DET.CUENTA,
    PLAN.NOMBRE,
    DET.MES,
  ]') || TO_CLOB(q'[  DET.ANO,
    DET.FUENTE_RECURSO,
     COM.DEPENDENCIA  || '-'   || DEPENDENCIA.NOMBRE,
    DET.SUBPROGRAMA,
    TO_CHAR(DET.FECHA, 'DD/MM/YYYY'),
    TIPO.NOMBRE,
    DET.FUENTE_RECURSO    || '-'    || FT.NOMBRE]') CONSULTA, 3 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);