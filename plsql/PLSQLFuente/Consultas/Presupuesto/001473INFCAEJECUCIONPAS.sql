WITH C_CAEJECUCIONPAS AS (
SELECT SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,
  SALDO_PLAN_PPTAL.CODIGO CUENTA,
  SUM(APROPIACION_DEBITO-APROPIACION_CREDITO) APROPIADO,
  SUM(EJE_PPT_DEBITO-EJE_PPT_CREDITO) TOTGIROS,
  SUM(SALDO_PLAN_PPTAL.ADICION)  ADICION,
  PLAN_PRESUPUESTAL.NOMBRE,
  PLAN_PRESUPUESTAL.CODIGO ID,
  SUM(SALDO_PLAN_PPTAL.REDUCCION) REDUCCION,
  SUM(TRASLADO_DEBITO +APLAZAM_DEBITO)  CREDITOS,
  SUM(TRASLADO_CREDITO+APLAZAM_CREDITO) CONTRACREDITOSINI,
  SUM(TRASLADO_CREDITO+APLAZAM_CREDITO)+ ABS(SUM(SALDO_PLAN_PPTAL.REDUCCION))  CONTRACREDITOS,
  (SUM(APROPIACION_DEBITO-APROPIACION_CREDITO)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(TRASLADO_DEBITO +APLAZAM_DEBITO))-(SUM(TRASLADO_CREDITO+APLAZAM_CREDITO)+ ABS(SUM(SALDO_PLAN_PPTAL.REDUCCION))) APROPIADOTOT,
  PLAN_PRESUPUESTAL.DESTINO,
  PLAN_PRESUPUESTAL.NIVEL1,
  PLAN_PRESUPUESTAL.NIVEL2,
  PLAN_PRESUPUESTAL.NIVEL3,
  PLAN_PRESUPUESTAL.NIVEL4,
  PLAN_PRESUPUESTAL.NIVEL5,
  PLAN_PRESUPUESTAL.NIVEL6
FROM PLAN_PRESUPUESTAL
  INNER JOIN SALDO_PLAN_PPTAL
  ON PLAN_PRESUPUESTAL.CODIGO  = SALDO_PLAN_PPTAL.CODIGO
  AND PLAN_PRESUPUESTAL.ANO  = SALDO_PLAN_PPTAL.ANO
  AND PLAN_PRESUPUESTAL.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
WHERE SALDO_PLAN_PPTAL.COMPANIA = 's$compania$s'
AND SALDO_PLAN_PPTAL.ANO = s$anio$s
AND SALDO_PLAN_PPTAL.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
AND PLAN_PRESUPUESTAL.NATURALEZA= 'D'
AND SALDO_PLAN_PPTAL.MES BETWEEN 0 AND s$mes$s
GROUP BY SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,
  SALDO_PLAN_PPTAL.CODIGO,
  PLAN_PRESUPUESTAL.NOMBRE,
  PLAN_PRESUPUESTAL.CODIGO,
  PLAN_PRESUPUESTAL.NATURALEZA,
  PLAN_PRESUPUESTAL.DESTINO,
  PLAN_PRESUPUESTAL.NIVEL1,
  PLAN_PRESUPUESTAL.NIVEL2,
  PLAN_PRESUPUESTAL.NIVEL3,
  PLAN_PRESUPUESTAL.NIVEL4,
  PLAN_PRESUPUESTAL.NIVEL5,
  PLAN_PRESUPUESTAL.NIVEL6
)
SELECT 
  C_CAEJECUCIONPAS.DESTINO,
  C_CAEJECUCIONPAS.NIVEL1,
  C_CAEJECUCIONPAS.NIVEL2,
  C_CAEJECUCIONPAS.NIVEL3,
  C_CAEJECUCIONPAS.NIVEL4,
  C_CAEJECUCIONPAS.NIVEL5,
  C_CAEJECUCIONPAS.NIVEL6,
  C_CAEJECUCIONPAS.ID,
  C_CAEJECUCIONPAS.NOMBRE,
  C_CAEJECUCIONPAS.APROPIADO,
  C_CAEJECUCIONPAS.ADICION,
  C_CAEJECUCIONPAS.CREDITOS,
  C_CAEJECUCIONPAS.CONTRACREDITOS,
  C_CAEJECUCIONPAS.APROPIADOTOT,
  TOTGIROS-(EJE_PPT_DEBITO-EJE_PPT_CREDITO) GIROSANTER,
  EJE_PPT_DEBITO-EJE_PPT_CREDITO EJECUCIONPPT,
  C_CAEJECUCIONPAS.TOTGIROS TOTALGIROS,
  APROPIADOTOT-C_CAEJECUCIONPAS.TOTGIROS SALDOAPROPIADO 
FROM C_CAEJECUCIONPAS
  INNER JOIN SALDO_PLAN_PPTAL
  ON C_CAEJECUCIONPAS.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
  AND C_CAEJECUCIONPAS.ANO     = SALDO_PLAN_PPTAL.ANO
  AND C_CAEJECUCIONPAS.CUENTA  = SALDO_PLAN_PPTAL.CODIGO
WHERE SALDO_PLAN_PPTAL.MES= s$mes$s
ORDER BY SALDO_PLAN_PPTAL.CODIGO
