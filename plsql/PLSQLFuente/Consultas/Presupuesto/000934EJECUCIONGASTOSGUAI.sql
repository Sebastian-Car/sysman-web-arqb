SELECT 	LPAD(SALDO_PLAN_PPTAL.ID,1)  CLASE,
        PLAN_PRESUPUESTAL.COMPANIA,
        PLAN_PRESUPUESTAL.ANO,
        PLAN_PRESUPUESTAL.ID,
        SALDO_PLAN_PPTAL.ID CUENTA,
        PLAN_PRESUPUESTAL.CODIGO,
        PLAN_PRESUPUESTAL.NOMBRE,
        SUM(APROPIACION_DEBITO-APROPIACION_CREDITO)  APROPIACIONANTERIOR,
        (SUM(SALDO_PLAN_PPTAL.REDUCCION)*-1)          TOTALREDUCCION,
        SUM(SALDO_PLAN_PPTAL.ADICION)  TOTALADICION,
        SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO)  TOTALDEBITO,
        SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO)  TOTALCREDITO,
        SUM(REGISTRO_OBLIGACION + MODIF_REGISTRO_OBLIGACION)  TOTALOBLIGACIONES,
        SUM(REG_CONTRACT + REG_NO_CONTRACT + MODIF_REG_CONT + MODIF_REG_NOCONT) TOTALCOMPROMISOS,
        SUM(EJE_PPT_DEBITO - EJE_PPT_CREDITO) TOTALPAGOS,
        SUM(SALDO_PLAN_PPTAL.APLAZAM_DEBITO)     SUMADEAPLAZAM_DEBITO,
        SUM(SALDO_PLAN_PPTAL.APLAZAM_CREDITO)   SUMADEAPLAZAM_CREDITO,
        SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD) TOTALDISPONIBILIDAD,
        SUM(APLAZAM_DEBITO - APLAZAM_CREDITO)*-1 TOTALAPLAZAMIENTO
    FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
      LEFT JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
        ON PLAN_PRESUPUESTAL.COMPANIA        = SALDO_PLAN_PPTAL.COMPANIA 
        AND PLAN_PRESUPUESTAL.ANO            = SALDO_PLAN_PPTAL.ANO
        AND PLAN_PRESUPUESTAL.ID             = SALDO_PLAN_PPTAL.ID
    WHERE PLAN_PRESUPUESTAL.COMPANIA = s$compania$s
      AND PLAN_PRESUPUESTAL.ANO    = s$ano$s
      AND SALDO_PLAN_PPTAL.MES     BETWEEN 0 AND  s$mesFinal$s
      AND PLAN_PRESUPUESTAL.TIPOVIGENCIA IN ('VF','VA')
      AND PLAN_PRESUPUESTAL.NATURALEZA    ='D'
      AND PLAN_PRESUPUESTAL.CENTRO_COSTO IS NULL 
      AND PLAN_PRESUPUESTAL.AUXILIAR IS NULL 
      AND PLAN_PRESUPUESTAL.ID BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
    GROUP BY 	PLAN_PRESUPUESTAL.COMPANIA,
          PLAN_PRESUPUESTAL.ANO,
          PLAN_PRESUPUESTAL.ID,
          SALDO_PLAN_PPTAL.ID,
          PLAN_PRESUPUESTAL.CODIGO,
          PLAN_PRESUPUESTAL.NOMBRE
