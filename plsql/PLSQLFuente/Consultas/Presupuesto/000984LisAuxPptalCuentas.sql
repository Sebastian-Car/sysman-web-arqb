SELECT 
  DETALLE_COMPROBANTE_PPTAL.COMPANIA,
  CASE WHEN s$tipoCuenta$s = 1 
     THEN DETALLE_COMPROBANTE_PPTAL.ID
     ELSE LPAD(NVL(V_PLAN_PRESUPUESTAL.NIVEL1,''),3) || ' -' || 
          LPAD(NVL(V_PLAN_PRESUPUESTAL.NIVEL2, ''),3) || ' -' ||
          LPAD(NVL(V_PLAN_PRESUPUESTAL.NIVEL3, ''), 3) || ' -' || 
          LPAD(NVL(V_PLAN_PRESUPUESTAL.NIVEL4, ''), 3) || ' -' ||
          LPAD(NVL(V_PLAN_PRESUPUESTAL.NIVEL5, ''),3) || ' -' ||
          LPAD(NVL(V_PLAN_PRESUPUESTAL.NIVEL6, ''), 3) || ' -' ||
          LPAD(NVL(V_PLAN_PRESUPUESTAL.NIVEL7, ''),3) END  RUBRO,
  DETALLE_COMPROBANTE_PPTAL.FECHA,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE, 
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE, 
  V_PLAN_PRESUPUESTAL.NOMBRE NOMBREPLAN,
  V_PLAN_PRESUPUESTAL.NATURALEZA, 
  TERCERO.NOMBRE NOMBRETERCERO,
DETALLE_COMPROBANTE_PPTAL.NRO_DOCUMENTO NUMERODOC,
  TIPO_COMPROBPP.NOMBRE,
  AUXILIAR.NOMBRE NOMBREAUXILIAR, 
  CASE WHEN s$tipoCuenta$s = 1 
     THEN V_PREDECESOR_PPTAL.PREDECESOR
     ELSE ''
     END ID_PREDE,
  CASE WHEN s$tipoCuenta$s = 1
     THEN V_PREDECESOR_PPTAL.PREDECESOR_NOMBRE
     ELSE ''
     END NOMBRE_PRED, 
  CENTRO_COSTO.NOMBRE NOMBRECENTROCOSTO,
  DETALLE_COMPROBANTE_PPTAL.TERCERO CODTERCERO,
  DETALLE_COMPROBANTE_PPTAL.AUXILIAR CODAUXILIAR, 
  DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO CODCENTROCOSTO, 
  TIPO_COMPROBPP.CLASE, 
  COMPROBANTE_PPTAL.TIPOCONTRATO||' - '||COMPROBANTE_PPTAL.NUMEROCONTRATO CONTRATO,
  COMPROBANTE_PPTAL.NUMEROCONTRATO, 
  COMPROBANTE_PPTAL.TIPOCONTRATO,
  DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO, 
  DETALLE_COMPROBANTE_PPTAL.TERCERO,
  COMPROBANTE_PPTAL.TIPO, 
  COMPROBANTE_PPTAL.NRO_DOCUMENTO, 
  CASE WHEN TIPO_COMPROBPP.CLASE='RES' THEN COMPROBANTE_PPTAL.DESCRIPCION||' '||COMPROBANTE_PPTAL.TEXTO ELSE COMPROBANTE_PPTAL.DESCRIPCION END DESCRIPCION,
  CASE WHEN DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT IS NOT NULL AND DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO <> 0 THEN DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT ||'-'|| DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO ELSE '' END DOCUMENTOAFECTADO, 
  SUM(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO) MODIFICACION_DEBITO1,
  SUM(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO) MODIFICACION_CREDITO1,
  SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO) VALORDEBITO,
  SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) VALORCREDITO,
  SUM(NVL(AFECTADO.VALOR,0)*NVL(AFECTADO.CUENTA,0))  DEBITO_AFECTADO,
  SUM(DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO) CREDITO_AFECTADO, 
  SUM(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO) MODIFICACION_DEBITO,
  SUM(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO) MODIFICACION_CREDITO,
  SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO)- (SUM(NVL(AFECTADO.VALOR,0)*NVL(AFECTADO.CUENTA,0))) + (SUM(DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO)) SALDOPOREJECUTARESP,
  SUM(CASE WHEN TIPO_COMPROBPP.CLASE<>'EGR' THEN CASE WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D' THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)-(DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO)+(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO) ELSE (DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO-DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO)-(DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO)+(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO)END ELSE 0 END) SALDOPOREJECUTAR
FROM DETALLE_COMPROBANTE_PPTAL 
  INNER JOIN AUXILIAR 
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = AUXILIAR.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.AUXILIAR = AUXILIAR.CODIGO
    AND DETALLE_COMPROBANTE_PPTAL.ANO = AUXILIAR.ANO
  INNER JOIN CENTRO_COSTO
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = CENTRO_COSTO.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO = CENTRO_COSTO.CODIGO
    AND DETALLE_COMPROBANTE_PPTAL.ANO = CENTRO_COSTO.ANO
  INNER JOIN COMPROBANTE_PPTAL 
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = COMPROBANTE_PPTAL.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO = COMPROBANTE_PPTAL.ANO
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = COMPROBANTE_PPTAL.TIPO
    AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO
  INNER JOIN V_PLAN_PRESUPUESTAL 
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = V_PLAN_PRESUPUESTAL.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO = V_PLAN_PRESUPUESTAL.ANO
    AND DETALLE_COMPROBANTE_PPTAL.ID = V_PLAN_PRESUPUESTAL.ID
INNER JOIN V_PREDECESOR_PPTAL
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = V_PREDECESOR_PPTAL.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO      = V_PREDECESOR_PPTAL.ANO
    AND DETALLE_COMPROBANTE_PPTAL.ID       = V_PREDECESOR_PPTAL.ID
  INNER JOIN TIPO_COMPROBPP 
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
  INNER JOIN TERCERO 
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = TERCERO.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.TERCERO = TERCERO.NIT
    AND DETALLE_COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL
  LEFT JOIN ( SELECT
                DCP.TIPO_CPTE,
                DCP.COMPROBANTE,
                DCP.ANO,
                DCP.ID,
                DCP.COMPANIA,
                DCP.ANO_AFECT,
                DCP.TIPO_CPTE_AFECT,
                DCP.CMPTE_AFECTADO,
                COUNT(*) CUENTA,
                SUM(CASE WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D' THEN DCP.VALOR_DEBITO-DCP.VALOR_CREDITO ELSE DCP.VALOR_CREDITO-DCP.VALOR_DEBITO END) VALOR
              FROM V_PLAN_PRESUPUESTAL
                LEFT JOIN DETALLE_COMPROBANTE_PPTAL DCP
                   ON V_PLAN_PRESUPUESTAL.COMPANIA = DCP.COMPANIA
                  AND V_PLAN_PRESUPUESTAL.ANO = DCP.ANO 
                  AND V_PLAN_PRESUPUESTAL.ID = DCP.ID
              WHERE DCP.COMPANIA = s$compania$s
                AND DCP.ANO = s$anio$s
                AND DCP.MES BETWEEN s$mesInicial$s AND s$mesFinal$s
              GROUP BY
                DCP.TIPO_CPTE,
                DCP.COMPROBANTE,
                DCP.ANO,
                DCP.ID,
                DCP.COMPANIA,
                DCP.ANO_AFECT,
                DCP.TIPO_CPTE_AFECT,
                DCP.CMPTE_AFECTADO) AFECTADO
      ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = AFECTADO.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO = AFECTADO.ANO_AFECT
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = AFECTADO.TIPO_CPTE_AFECT
    AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = AFECTADO.CMPTE_AFECTADO
    AND DETALLE_COMPROBANTE_PPTAL.ID = AFECTADO.ID
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA = s$compania$s
  AND DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO BETWEEN 's$centroInicial$s' AND 's$centroFinal$s' 
  AND DETALLE_COMPROBANTE_PPTAL.FECHA BETWEEN TO_DATE('s$fechaInicial$s', 'DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s', 'DD/MM/YYYY') 
  AND COMPROBANTE_PPTAL.TIPO BETWEEN 's$tipoInicial$s' AND 's$tipoFinal$s'  
  AND DETALLE_COMPROBANTE_PPTAL.CUENTA BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  AND DETALLE_COMPROBANTE_PPTAL.TERCERO BETWEEN 's$terceroInicial$s' AND 's$terceroFinal$s'
  s$sinMod$s
GROUP BY 
  DETALLE_COMPROBANTE_PPTAL.COMPANIA,
  DETALLE_COMPROBANTE_PPTAL.ID,
  DETALLE_COMPROBANTE_PPTAL.FECHA,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE, 
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE, 
  V_PLAN_PRESUPUESTAL.NOMBRE,
  V_PLAN_PRESUPUESTAL.NATURALEZA, 
  TERCERO.NOMBRE,
 V_PLAN_PRESUPUESTAL.NIVEL1,
  V_PLAN_PRESUPUESTAL.NIVEL2, 
  V_PLAN_PRESUPUESTAL.NIVEL3, 
  V_PLAN_PRESUPUESTAL.NIVEL4, 
  V_PLAN_PRESUPUESTAL.NIVEL5,
  V_PLAN_PRESUPUESTAL.NIVEL6, 
  V_PLAN_PRESUPUESTAL.NIVEL7,
  TIPO_COMPROBPP.NOMBRE,
V_PREDECESOR_PPTAL.PREDECESOR, 
  V_PREDECESOR_PPTAL.PREDECESOR_NOMBRE , 
  AUXILIAR.NOMBRE, 
  CENTRO_COSTO.NOMBRE,
  DETALLE_COMPROBANTE_PPTAL.AUXILIAR, 
  DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO, 
  DETALLE_COMPROBANTE_PPTAL.TERCERO,
  TIPO_COMPROBPP.CLASE, 
  COMPROBANTE_PPTAL.TIPOCONTRATO,
  COMPROBANTE_PPTAL.NUMEROCONTRATO,
  COMPROBANTE_PPTAL.NUMEROCONTRATO, 
  COMPROBANTE_PPTAL.TIPOCONTRATO,
  DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO, 
  COMPROBANTE_PPTAL.TIPO, 
  COMPROBANTE_PPTAL.NRO_DOCUMENTO,
  COMPROBANTE_PPTAL.DESCRIPCION,
DETALLE_COMPROBANTE_PPTAL.NRO_DOCUMENTO,
  COMPROBANTE_PPTAL.TEXTO,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT,
  DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO
ORDER BY DETALLE_COMPROBANTE_PPTAL.ID
