SELECT DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
    TO_CHAR(DETALLE_COMPROBANTE_PPTAL.FECHA, 'DD/MM/YYYY') AS FECHA
,
    DETALLE_COMPROBANTE_PPTAL.CUENTA AS CODIGO_CUENTA,
    DETALLE_COMPROBANTE_PPTAL.ID AS CUENTA,
    V_PLAN_PRESUPUESTAL.NOMBRE AS NOMBRECTA,
    DETALLE_COMPROBANTE_PPTAL.DESCRIPCION,
    DETALLE_COMPROBANTE_PPTAL.PAC_DISPONIBLE,
    DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO,
    TERCERO.NIT    AS TERCERO,
    TERCERO.NOMBRE AS NOMBRETERCERO,
    TERCERO.SUCURSAL
FROM DETALLE_COMPROBANTE_PPTAL LEFT JOIN TERCERO ON 
    DETALLE_COMPROBANTE_PPTAL.COMPANIA      = TERCERO.COMPANIA 
    AND DETALLE_COMPROBANTE_PPTAL.TERCERO   = TERCERO.NIT 
    AND DETALLE_COMPROBANTE_PPTAL.SUCURSAL  = TERCERO.SUCURSAL
LEFT JOIN V_PLAN_PRESUPUESTAL ON 
    DETALLE_COMPROBANTE_PPTAL.COMPANIA    = V_PLAN_PRESUPUESTAL.COMPANIA 
    AND DETALLE_COMPROBANTE_PPTAL.ANO     = V_PLAN_PRESUPUESTAL.ANO 
    AND DETALLE_COMPROBANTE_PPTAL.ID      = V_PLAN_PRESUPUESTAL.ID
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA      = s$compania$s
    AND DETALLE_COMPROBANTE_PPTAL.ANO         = s$ano$s
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = 's$tipoCpte$s'
    AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE BETWEEN 's$numeroPptoInicial$s' AND 's$numeroPptoFinal$s'
