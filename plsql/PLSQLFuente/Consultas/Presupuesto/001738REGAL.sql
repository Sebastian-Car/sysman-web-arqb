SELECT V_DETALLE_AUXILIAR_PPTAL.TIPO_COMPROBANTE_NOM NOMBRECOMPROBANTE,
  V_DETALLE_AUXILIAR_PPTAL.COMPROBANTE,
  TO_CHAR(V_DETALLE_AUXILIAR_PPTAL.FECHA,'DD/MM/YYYY') FECHADETALLE,
  V_DETALLE_AUXILIAR_PPTAL.TERCERO_NOM NOMBRETERCERO,
  V_DETALLE_AUXILIAR_PPTAL.TERCERO_DET TERCERODETCOMPROBANTE,
  V_DETALLE_AUXILIAR_PPTAL.CUENTA CODIGO_CUENTA,
  V_DETALLE_AUXILIAR_PPTAL.CUENTANOMBRE NOMBREPLANPPTAL,
  V_DETALLE_AUXILIAR_PPTAL.DESCRIPCION_DET DESCRIPCIONDETALLECOMPR,
  (CASE
    WHEN V_DETALLE_AUXILIAR_PPTAL.VALOR_DEBITO = 0
    THEN V_DETALLE_AUXILIAR_PPTAL.VALOR_CREDITO
    ELSE V_DETALLE_AUXILIAR_PPTAL.VALOR_DEBITO
  END) VALORCOMPROBANTE,
  COMPROBANTE_PPTALAFECTADOS.TIPO_CPTE_AFECT,
  COMPROBANTE_PPTALAFECTADOS.COMPROBANTE_AFECT CMPTE_AFECTADO,
  EXTRACT(DAY FROM V_DETALLE_AUXILIAR_PPTAL.FECHA) DIAACTUAL,
  PCK_SYSMAN_UTL.FC_NOMBRE_MES(EXTRACT(MONTH FROM V_DETALLE_AUXILIAR_PPTAL.FECHA)) MESACTUAL,
  EXTRACT(YEAR FROM V_DETALLE_AUXILIAR_PPTAL.FECHA) ANIOACTUAL
FROM V_DETALLE_AUXILIAR_PPTAL
INNER JOIN COMPROBANTE_PPTALAFECTADOS
  ON V_DETALLE_AUXILIAR_PPTAL.COMPANIA        = COMPROBANTE_PPTALAFECTADOS.COMPANIA
  AND V_DETALLE_AUXILIAR_PPTAL.ANO            = COMPROBANTE_PPTALAFECTADOS.ANO
  AND V_DETALLE_AUXILIAR_PPTAL.TIPO           = COMPROBANTE_PPTALAFECTADOS.TIPO_CPTE
  AND V_DETALLE_AUXILIAR_PPTAL.COMPROBANTE    = COMPROBANTE_PPTALAFECTADOS.COMPROBANTE
WHERE V_DETALLE_AUXILIAR_PPTAL.COMPANIA       = s$compania$s
  AND V_DETALLE_AUXILIAR_PPTAL.ANO            = s$ano$s
  AND V_DETALLE_AUXILIAR_PPTAL.TIPO           = 's$tipoCpte$s'
  AND V_DETALLE_AUXILIAR_PPTAL.COMPROBANTE BETWEEN s$numeroPptoInicial$s AND s$numeroPptoFinal$s
