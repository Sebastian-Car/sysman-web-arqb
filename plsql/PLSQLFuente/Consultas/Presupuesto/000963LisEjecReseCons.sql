SELECT  
  SP.COMPANIA,
  SP.ID, 
  SP.ANO,
  P.CODIGO,
  P.NOMBRE,
  P.NATURALEZA,
  SUM(CASE WHEN MES = s$mes$s THEN SP.EJE_PPT_DEBITO - SP.EJE_PPT_CREDITO END)PAGOSTESORERIAMES,
  SUM(SP.APROPIACION_DEBITO - SP.APROPIACION_CREDITO) AS APROPIADO,
  SUM(SP.ADICION + (SP.TRASLADO_DEBITO - SP.TRASLADO_CREDITO) + SP.REDUCCION +  (SP.APLAZAM_DEBITO - SP.APLAZAM_CREDITO)) AS MODIFICACIONES,
  SUM(SP.APROPIACION_DEBITO - SP.APROPIACION_CREDITO) + SUM(SP.ADICION) + SUM(SP.TRASLADO_DEBITO - SP.TRASLADO_CREDITO) + SUM(SP.REDUCCION) +  SUM(SP.APLAZAM_DEBITO - SP.APLAZAM_CREDITO) AS RESERVADEFINITIVA,
  SUM(SP.REG_CONTRACT) + SUM(SP.REG_NO_CONTRACT) + SUM(SP.MODIF_REG_CONT) + SUM(SP.MODIF_REG_NOCONT) AS COMPROMISOS,
  SUM(SP.EJE_PPT_DEBITO - SP.EJE_PPT_CREDITO) AS PAGOSTES,
  (SUM(SP.APROPIACION_DEBITO - SP.APROPIACION_CREDITO) + SUM(SP.ADICION) + SUM(SP.TRASLADO_DEBITO - SP.TRASLADO_CREDITO) + SUM(SP.REDUCCION) +  SUM(SP.APLAZAM_DEBITO - SP.APLAZAM_CREDITO)) - SUM(SP.EJE_PPT_DEBITO - SP.EJE_PPT_CREDITO) AS SALDOAPROPIACION
FROM V_PLAN_PRESUPUESTAL P
INNER JOIN V_SALDO_PLAN_PPTAL SP
ON (P.ANO = SP.ANO)
AND (P.COMPANIA = SP.COMPANIA)
AND (P.ID = SP.ID)
WHERE 
SP.MES BETWEEN 0 AND s$mes$s
AND SP.COMPANIA = s$compania$s
AND SP.ANO = s$anio$s
AND SP.ID BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
AND P.NATURALEZA = 'D'
AND (P.TIPOVIGENCIA = 'RA' OR P.TIPOVIGENCIA = 'RC')
GROUP BY SP.COMPANIA,
  SP.ID,
  SP.ANO,
  P.CODIGO,
  P.NOMBRE,
  P.NATURALEZA
