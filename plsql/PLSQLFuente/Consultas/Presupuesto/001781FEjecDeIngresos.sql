SELECT COMPANIA,
  ANO,
  CUENTA,
  NOMBRE,
  SUM(ADIC) ADIC,
  SUM(RED) RED,
  SUM(APLAZAMIENTOS) APLAZAMIENTOS,
  SUM(TRASLADOS) TRASLADOS,
  SUM(MODIFICACIONES) MODIFICACIONES,
  SUM(PPTODEFINITIVO + PPTOINICIAL ) PPTODEFINITIVO,
  SUM(NVL(ENE,0)) ENE ,
  SUM(NVL(FEB,0)) FEB ,
  SUM(NVL(MAR,0)) MAR ,
  SUM(NVL(ABR,0)) ABR,
  SUM(NVL(MAY,0)) MAY,
  SUM(NVL(JUN,0)) JUN,
  SUM(NVL(JUL,0)) JUL,
  SUM(NVL(AGO,0)) AGO,
  SUM(NVL(SEP,0))SEP,
  SUM(NVL(OCT,0)) OCT,
  SUM(NVL(NOV,0)) NOV,
  SUM(NVL(DIC,0)) DIC
FROM
  (SELECT SALDO_PLAN_PPTAL.COMPANIA,
    SALDO_PLAN_PPTAL.ANO,
    SALDO_PLAN_PPTAL.ID CUENTA,
    PLAN_PRESUPUESTAL.NOMBRE,
    SALDO_PLAN_PPTAL.MES,   
    
    SUM(
    CASE
      WHEN PLAN_PRESUPUESTAL.NATURALEZA     IN ('D')
      THEN EJE_PPT_DEBITO  - EJE_PPT_CREDITO
      ELSE EJE_PPT_CREDITO - EJE_PPT_DEBITO
    END                    + MODIF_INGRESOS) VALOR,
    SUM(
    CASE
      WHEN PLAN_PRESUPUESTAL.NATURALEZA        IN ('D')
      THEN APROPIACION_DEBITO -APROPIACION_CREDITO
      ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
    END) PPTOINICIAL,  
    
    SUM(SALDO_PLAN_PPTAL.ADICION) ADIC,
    SUM(SALDO_PLAN_PPTAL.REDUCCION) RED,
    SUM(
    CASE
      WHEN PLAN_PRESUPUESTAL.NATURALEZA    IN ('D')
      THEN APLAZAM_DEBITO - APLAZAM_CREDITO
      ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
    END) APLAZAMIENTOS,
    SUM(
    CASE
      WHEN PLAN_PRESUPUESTAL.NATURALEZA      IN ('D')
      THEN TRASLADO_DEBITO  - TRASLADO_CREDITO
      ELSE TRASLADO_CREDITO - TRASLADO_DEBITO
    END) TRASLADOS,
    SUM(SALDO_PLAN_PPTAL.ADICION) + SUM(SALDO_PLAN_PPTAL.REDUCCION) + SUM(
    CASE
      WHEN PLAN_PRESUPUESTAL.NATURALEZA      IN ('D')
      THEN TRASLADO_DEBITO  - TRASLADO_CREDITO
      ELSE TRASLADO_CREDITO - TRASLADO_DEBITO
    END)                    + SUM(
    CASE
      WHEN PLAN_PRESUPUESTAL.NATURALEZA    IN ('D')
      THEN APLAZAM_DEBITO - APLAZAM_CREDITO
      ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
    END) MODIFICACIONES,
    
    SUM(SALDO_PLAN_PPTAL.ADICION) + SUM(SALDO_PLAN_PPTAL.REDUCCION) + SUM(
    CASE
      WHEN PLAN_PRESUPUESTAL.NATURALEZA      IN ('D')
      THEN TRASLADO_DEBITO  - TRASLADO_CREDITO
      ELSE TRASLADO_CREDITO - TRASLADO_DEBITO
    END)                    + SUM(
    CASE
      WHEN PLAN_PRESUPUESTAL.NATURALEZA    IN ('D')
      THEN APLAZAM_DEBITO - APLAZAM_CREDITO
      ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
    END)PPTODEFINITIVO  
  FROM PLAN_PRESUPUESTAL
  INNER JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
      ON PLAN_PRESUPUESTAL.COMPANIA    = SALDO_PLAN_PPTAL.COMPANIA
      AND PLAN_PRESUPUESTAL.CODIGO     = SALDO_PLAN_PPTAL.CODIGO
      AND PLAN_PRESUPUESTAL.ANO        = SALDO_PLAN_PPTAL.ANO
  WHERE PLAN_PRESUPUESTAL.COMPANIA = s$compania$s
  AND SALDO_PLAN_PPTAL.MES BETWEEN 0 AND 12
  AND SALDO_PLAN_PPTAL.ANO = s$anio$s
  AND SALDO_PLAN_PPTAL.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  AND PLAN_PRESUPUESTAL.NATURALEZA IN ('C')
  GROUP BY SALDO_PLAN_PPTAL.COMPANIA,
    SALDO_PLAN_PPTAL.ANO,
    SALDO_PLAN_PPTAL.ID,
    PLAN_PRESUPUESTAL.NOMBRE,
    PLAN_PRESUPUESTAL.NATURALEZA,
    SALDO_PLAN_PPTAL.MES
  ) PIVOT (SUM(VALOR) FOR MES IN (0 INI, 1 ENE ,2 FEB ,3 MAR,4 ABR,5 MAY,6 JUN,7 JUL,8 AGO,9 SEP,10 OCT,11 NOV,12 DIC, 13 FIN) )
  GROUP BY COMPANIA,
  ANO,
  CUENTA,
  NOMBRE
ORDER BY ANO, CUENTA
