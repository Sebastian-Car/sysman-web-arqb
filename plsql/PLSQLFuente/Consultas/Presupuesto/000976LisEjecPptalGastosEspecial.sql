SELECT  COMPANIA,
        CODIGO,
        NOMBRE,
        ID CUENTA,
        SUM(APROPIADO) APROPIADO,
        SUM(ADICION + TRASLADO_DEBITO + APLAZAM_DEBITO)MODIFADICION,
        ABS(SUM(REDUCCION + TRASLADO_CREDITO + APLAZAM_CREDITO))MODIFREDUCCION,
        SUM(APROPIACIONVIGENTE)TOTAL,
        SUM(COMPROMISOSACUM)RESERVAS,
        SUM(DISPONIBILIDAD)DISPONIBILIDADESACUMULADAS,
        SUM(APROPIACIONVIGENTE - COMPROMISOSACUM)SALDOPORCOMPROMETER,
        SUM(APROPIACIONVIGENTE - DISPONIBILIDAD)SALDODEAPROPIACION
FROM V_RESUMENPPTO_BASE
WHERE COMPANIA = s$compania$s
  AND ANO = s$anio$s
  AND MES <= s$mes$s
  AND NATURALEZA IN ('D')
  AND ID BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  AND LENGTH(ID)<= s$nivel$s
GROUP BY   COMPANIA,
        CODIGO,
        NOMBRE,
        ID
