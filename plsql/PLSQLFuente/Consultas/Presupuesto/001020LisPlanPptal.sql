SELECT 
  PLAN_PRESUPUESTAL.ANO,
  PLAN_PRESUPUESTAL.CODIGO,
  PLAN_PRESUPUESTAL.NOMBRE PLAN_PPTAL_NOM,
  CASE WHEN PLAN_PRESUPUESTAL.MOVIMIENTO NOT IN (0)
       THEN 'X'
       ELSE ' '
       END MOVIMIENTO,
  CASE WHEN PLAN_PRESUPUESTAL.MAN_CEN_CTO NOT IN (0)
       THEN 'X'
       ELSE ' '
       END MAN_CEN_CTO,
  CASE WHEN PLAN_PRESUPUESTAL.MAN_AUX_TER NOT IN (0)
       THEN 'X'
       ELSE ' '
       END MAN_AUX_TER,
  CASE WHEN PLAN_PRESUPUESTAL.MAN_AUX_GEN NOT IN (0)
       THEN 'X'
       ELSE ' '
       END MAN_AUX_GEN,
  CASE WHEN PLAN_PRESUPUESTAL.MAN_PAC NOT IN (0)
       THEN 'X'
       ELSE ' '
       END MAN_PAC,
  CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA ='D'
       THEN 'Gastos'
       ELSE 'Ingresos'
       END NATURALEZA, 
  PLAN_PRESUPUESTAL.CODIGO_EQUIV,
  DEPENDENCIA.NOMBRE DEPENDENCIA_NOM 
FROM PLAN_PRESUPUESTAL 
LEFT JOIN DEPENDENCIA 
ON  PLAN_PRESUPUESTAL.COMPANIA            = DEPENDENCIA.COMPANIA  
AND PLAN_PRESUPUESTAL.DEPENDENCIAASOCIADA = DEPENDENCIA.CODIGO
WHERE PLAN_PRESUPUESTAL.COMPANIA  = s$compania$s
  AND PLAN_PRESUPUESTAL.ANO       = s$anio$s
  AND PLAN_PRESUPUESTAL.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  AND LENGTH(PLAN_PRESUPUESTAL.CODIGO) <= s$nivel$s
