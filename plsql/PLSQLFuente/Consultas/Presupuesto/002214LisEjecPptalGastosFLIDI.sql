SELECT * FROM 
( 
SELECT  V_RESUMENPPTO_BASE.COMPANIA, 
        V_RESUMENPPTO_BASE.ANO, 
        V_RESUMENPPTO_BASE.NOMBRE, 
        V_RESUMENPPTO_BASE.ID CUENTA, 
        V_RESUMENPPTO_BASE.CODIGO, 
        V_RESUMENPPTO_BASE.AUXILIAR, 
        V_RESUMENPPTO_BASE.FUENTE_RECURSO, 
        V_RESUMENPPTO_BASE.MOVIMIENTO,  
        TRUNC(SUM(V_RESUMENPPTO_BASE.APROPIADO) * POWER(10,s$digitos$s)) / POWER(10,s$digitos$s) AS APROPIADO1,
        TRUNC(SUM(V_RESUMENPPTO_BASE.OBLIGACIONESACUM) * POWER(10,s$digitos$s)) / POWER(10,s$digitos$s) AS REGISTROOBLIGACION1,
        TRUNC(SUM(V_RESUMENPPTO_BASE.ADICION  + V_RESUMENPPTO_BASE.REDUCCION + 
                  V_RESUMENPPTO_BASE.TRASLADO + V_RESUMENPPTO_BASE.APLAZAMIENTO) * POWER(10,s$digitos$s)) / POWER(10,s$digitos$s) AS MODIFICACIONES1,
                  SUM(V_RESUMENPPTO_BASE.ADICION  + V_RESUMENPPTO_BASE.REDUCCION + 
            V_RESUMENPPTO_BASE.TRASLADO + V_RESUMENPPTO_BASE.APLAZAMIENTO)MODIFICACIONES2, 
        TRUNC(SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE) * POWER(10,s$digitos$s)) / POWER(10,s$digitos$s) TOTALAPROPIADO1, 
        TRUNC(SUM(V_RESUMENPPTO_BASE.DISPONIBILIDAD)  * POWER(10,s$digitos$s)) / POWER(10,s$digitos$s)AS DISPONIBILIDADES1, 
        TRUNC(SUM(V_RESUMENPPTO_BASE.COMPROMISOSACUM) * POWER(10,s$digitos$s)) / POWER(10,s$digitos$s) AS RESERVAS1,
        TRUNC(ROUND(SUM(V_RESUMENPPTO_BASE.DISPONIBILIDAD - V_RESUMENPPTO_BASE.COMPROMISOSACUM),s$digitos$s) * POWER(10,s$digitos$s)) / POWER(10,s$digitos$s) AS TOTAL1,
        
        TRUNC(SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT - CASE WHEN V_RESUMENPPTO_BASE.MES = s$mesFinal$s THEN V_RESUMENPPTO_BASE.EJECUCIONPPT ELSE 0 END)* POWER(10,s$digitos$s)) / POWER(10,2)PAGOSANTERIORES1, 
        TRUNC(SUM(CASE WHEN V_RESUMENPPTO_BASE.MES = s$mesFinal$s THEN V_RESUMENPPTO_BASE.EJECUCIONPPT ELSE 0 END)* POWER(10,s$digitos$s)) / POWER(10,s$digitos$s) PAGOSMES1,
        
        TRUNC(SUM(V_RESUMENPPTO_BASE.OBLIGACIONESACUM  - V_RESUMENPPTO_BASE.EJECUCIONPPT)* POWER(10,s$digitos$s)) / POWER(10,s$digitos$s)CUENTASPORPAGAR1,
        
        TRUNC(SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT)* POWER(10,s$digitos$s)) / POWER(10,s$digitos$s) AS PAGOSACUMULADOS1,
        SUM(CASE WHEN V_RESUMENPPTO_BASE.MES = s$mesFinal$s THEN V_RESUMENPPTO_BASE.EJECUCIONPPT ELSE 0 END)PAGOSMES, 
        SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT - CASE WHEN V_RESUMENPPTO_BASE.MES = s$mesFinal$s THEN V_RESUMENPPTO_BASE.EJECUCIONPPT ELSE 0 END)PAGOSANTERIORES,         
        
        SUM(V_RESUMENPPTO_BASE.DISPONIBILIDAD)DISPONIBILIDADES,
        SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE)TOTALAPROPIADO, 
        CASE WHEN SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE) > 0 
              THEN SUM(V_RESUMENPPTO_BASE.COMPROMISOSACUM) / SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE)*100 
              ELSE 0 
          END COMPR,   
        SUM(V_RESUMENPPTO_BASE.DISPONIBILIDAD - V_RESUMENPPTO_BASE.COMPROMISOSACUM) AS SALDODEDISPONIBILIDADES, 
        SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE - V_RESUMENPPTO_BASE.COMPROMISOSACUM) AS SALDODEAPROPIACION, 
        SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT) AS PAGOSACUMULADOS,  
        SUM(V_RESUMENPPTO_BASE.OBLIGACIONESACUM  - V_RESUMENPPTO_BASE.EJECUCIONPPT)  AS CUENTASPORPAGAR, 
        TRUNC(SUM(CASE WHEN V_RESUMENPPTO_BASE.MES = s$mesFinal$s THEN V_RESUMENPPTO_BASE.REG_CONTRACT + V_RESUMENPPTO_BASE.REG_NO_CONTRACT + V_RESUMENPPTO_BASE.MODIF_REG_CONT + V_RESUMENPPTO_BASE.MODIF_REG_NOCONT ELSE 0 END)* POWER(10,2)) / POWER(10,2) COMPROMETIDOMES, 
        SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE - V_RESUMENPPTO_BASE.OBLIGACIONESACUM) RESERVASEXCEPCIONALES,   
        SUM(V_RESUMENPPTO_BASE.COMPROMISOSACUM)RESERVAS, 
        SUM(V_RESUMENPPTO_BASE.ADICION  + V_RESUMENPPTO_BASE.REDUCCION + 
            V_RESUMENPPTO_BASE.TRASLADO + V_RESUMENPPTO_BASE.APLAZAMIENTO)MODIFICACIONES, 
        SUM(V_RESUMENPPTO_BASE.APROPIADO)APROPIADO,
        SUM(CASE WHEN V_RESUMENPPTO_BASE.MES = s$mesFinal$s THEN V_RESUMENPPTO_BASE.TOTALREO ELSE 0 END) TOTALREO ,	
        SUM(CASE WHEN V_RESUMENPPTO_BASE.MES = s$mesFinal$s THEN V_RESUMENPPTO_BASE.REG_CONTRACT + V_RESUMENPPTO_BASE.REG_NO_CONTRACT + V_RESUMENPPTO_BASE.MODIF_REG_CONT + V_RESUMENPPTO_BASE.MODIF_REG_NOCONT ELSE 0 END) AS RESERVASM
FROM V_RESUMENPPTO_BASE  
WHERE V_RESUMENPPTO_BASE.COMPANIA = s$compania$s 
    AND V_RESUMENPPTO_BASE.ANO = s$ano$s 
    AND V_RESUMENPPTO_BASE.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
    --AND V_RESUMENPPTO_BASE.MES BETWEEN s$mesInicial$s AND s$mesFinal$s
    AND V_RESUMENPPTO_BASE.MES <= s$mesFinal$s 
    --AND V_RESUMENPPTO_BASE.MES <= s$mesInicial$s 
    AND V_RESUMENPPTO_BASE.NATURALEZA IN ('D') 
    AND LENGTH(V_RESUMENPPTO_BASE.CODIGO) BETWEEN 0 AND NVL(s$nivel$s,6) 
GROUP BY    V_RESUMENPPTO_BASE.COMPANIA, 
            V_RESUMENPPTO_BASE.ANO, 
            V_RESUMENPPTO_BASE.NOMBRE, 
            V_RESUMENPPTO_BASE.ID , 
            V_RESUMENPPTO_BASE.CODIGO, 
            V_RESUMENPPTO_BASE.AUXILIAR, 
            V_RESUMENPPTO_BASE.FUENTE_RECURSO, 
            V_RESUMENPPTO_BASE.MOVIMIENTO 
) 
 
WHERE ABS(APROPIADO1) + ABS(MODIFICACIONES1) + ABS(TOTALAPROPIADO1) + ABS(DISPONIBILIDADES1) +  ABS(RESERVAS1) + ABS(TOTAL1) + ABS(REGISTROOBLIGACION1) + 
    ABS(PAGOSANTERIORES1) + ABS(PAGOSMES1) +  ABS(PAGOSACUMULADOS1) + ABS(CUENTASPORPAGAR) +  ABS(RESERVAS1) + ABS(CUENTASPORPAGAR1) > 0