SELECT V_PLAN_PRESUPUESTAL.CODIGO RUBRO,
V_PLAN_PRESUPUESTAL.FUENTE_RECURSO FUENTE,
V_PLAN_PRESUPUESTAL.NOMBRE DESCRIPCION_RUBRO,
FUENTE_RECURSOS.NOMBRE DESCRIPCION_FUENTE,
SUM(SALDOS.APROPIACIONANTERIOR  ) APROPIACION_INICIAL,
SUM(SALDOS.ADICION              ) ADICIONES,
SUM(SALDOS.REDUCCION            ) REDUCCIONES,
SUM(SALDOS.TRASLADO_DEBITO      ) CREDITOS,
SUM(SALDOS.TRASLADO_CREDITO     ) CONTRA_CREDITO,
SUM(SALDOS.DEFINITIVO           ) APROPIACION_DEFINITIVA,
SUM(SALDOS.SUMADEAPLAZAM_DEBITO ) APLAZAMIENTOS,
SUM(SALDOS.SUMADEAPLAZAM_CREDITO) DESPLAZAMIENTO,
SUM(SALDOS.DISPONIBILIDAD       ) TOTAL_APROPIADO_DISPONIBILIDAD,
SUM(SALDOS.DEFINITIVO - SALDOS.DISPONIBILIDAD) AS SALDODISPONIBLE,
SUM(SALDOS.DISPONIBILIDAD       ) DISPONIBILIDADADES,
SUM(SALDOS.ABIERTAS             ) DISPONIBILIDADES_ABIERTAS,
SUM(SALDOS.COMPROMISO           ) COMPROMISOS,
SUM(SALDOS.COMPROMISOS_MES            ) COMPROMISOS_ABIERTOS,
SUM(SALDOS.OBLIGACIONES         ) OBLIGACIONES,
SUM(SALDOS.OBLIGACIONES_MES            ) OBLIGACIONES_ABIERTAS,
SUM(SALDOS.EJECUCION            ) PAGOS
FROM V_PLAN_PRESUPUESTAL
LEFT JOIN FUENTE_RECURSOS
    ON V_PLAN_PRESUPUESTAL.COMPANIA  = FUENTE_RECURSOS.COMPANIA
    AND V_PLAN_PRESUPUESTAL.FUENTE_RECURSO = FUENTE_RECURSOS.CODIGO
    AND V_PLAN_PRESUPUESTAL.ANO      = FUENTE_RECURSOS.ANO
INNER JOIN (
SELECT PLAN_PRESUPUESTAL.COMPANIA,
       PLAN_PRESUPUESTAL.ANO,
       SALDO_AUX_PPTAL.ID,
       SUM(CASE WHEN SALDO_AUX_PPTAL.MES < s$mesInicial$s 
                THEN CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' 
                          THEN   APROPIACION_DEBITO  - APROPIACION_CREDITO - TRASLADO_CREDITO + TRASLADO_DEBITO 
                               + APLAZAM_DEBITO      - APLAZAM_CREDITO
                          ELSE - APROPIACION_DEBITO  + APROPIACION_CREDITO + TRASLADO_CREDITO - TRASLADO_DEBITO 
                               - APLAZAM_DEBITO      + APLAZAM_CREDITO 
                          END + ADICION + REDUCCION
                ELSE 0 END
            ) AS APROPIACIONANTERIOR,
       SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' 
                          THEN   APROPIACION_DEBITO  - APROPIACION_CREDITO - TRASLADO_CREDITO + TRASLADO_DEBITO 
                               + APLAZAM_DEBITO      - APLAZAM_CREDITO
                          ELSE - APROPIACION_DEBITO  + APROPIACION_CREDITO + TRASLADO_CREDITO - TRASLADO_DEBITO 
                               - APLAZAM_DEBITO      + APLAZAM_CREDITO 
                          END + ADICION + REDUCCION
           ) AS DEFINITIVO,  
       SUM(CASE WHEN SALDO_AUX_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s 
                THEN SALDO_AUX_PPTAL.REDUCCION*-1 
                ELSE 0 END)  REDUCCION,
       SUM(CASE WHEN SALDO_AUX_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s 
                THEN SALDO_AUX_PPTAL.ADICION 
                ELSE 0 END)  ADICION,
       SUM(CASE WHEN SALDO_AUX_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s 
                THEN SALDO_AUX_PPTAL.TRASLADO_DEBITO 
                ELSE 0 END)  TRASLADO_DEBITO,
       SUM(CASE WHEN SALDO_AUX_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s 
                THEN SALDO_AUX_PPTAL.TRASLADO_CREDITO 
                ELSE 0 END)  TRASLADO_CREDITO,
       SUM(SALDO_AUX_PPTAL.REGISTRO_OBLIGACION + SALDO_AUX_PPTAL.MODIF_REGISTRO_OBLIGACION)  OBLIGACIONES,                
       SUM(SALDO_AUX_PPTAL.REG_CONTRACT 
               + SALDO_AUX_PPTAL.REG_NO_CONTRACT
               + SALDO_AUX_PPTAL.MODIF_REG_CONT
               + SALDO_AUX_PPTAL.MODIF_REG_NOCONT
            )  COMPROMISO,
       SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' 
                THEN   SALDO_AUX_PPTAL.EJE_PPT_DEBITO - SALDO_AUX_PPTAL.EJE_PPT_CREDITO
                ELSE - SALDO_AUX_PPTAL.EJE_PPT_DEBITO + SALDO_AUX_PPTAL.EJE_PPT_CREDITO
                END 
          )  EJECUCION,
       SUM(CASE WHEN SALDO_AUX_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s 
                THEN SALDO_AUX_PPTAL.APLAZAM_CREDITO
                ELSE 0 END)  SUMADEAPLAZAM_DEBITO,
       SUM(CASE WHEN SALDO_AUX_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s 
                THEN SALDO_AUX_PPTAL.APLAZAM_DEBITO
                ELSE 0 END)  SUMADEAPLAZAM_CREDITO,
       SUM(SALDO_AUX_PPTAL.DISPONIBILIDAD
           )  DISPONIBILIDAD,
     SUM(CASE WHEN SALDO_AUX_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s 
                THEN SALDO_AUX_PPTAL.DISPONIBILIDAD
                ELSE 0 END)  DISPONIBILIDAD_MES,           
     SUM(CASE WHEN SALDO_AUX_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s
                THEN  SALDO_AUX_PPTAL.REG_CONTRACT 
                     + SALDO_AUX_PPTAL.REG_NO_CONTRACT
                     + SALDO_AUX_PPTAL.MODIF_REG_CONT
                     + SALDO_AUX_PPTAL.MODIF_REG_NOCONT
                ELSE 0 END) COMPROMISOS_MES,
        SUM(CASE WHEN SALDO_AUX_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s
                  THEN SALDO_AUX_PPTAL.REGISTRO_OBLIGACION + SALDO_AUX_PPTAL.MODIF_REGISTRO_OBLIGACION          
                  ELSE 0 END) OBLIGACIONES_MES,
        SUM(CASE WHEN SALDO_AUX_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s
                    THEN (CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' 
                        THEN   SALDO_AUX_PPTAL.EJE_PPT_DEBITO - SALDO_AUX_PPTAL.EJE_PPT_CREDITO
                        ELSE - SALDO_AUX_PPTAL.EJE_PPT_DEBITO + SALDO_AUX_PPTAL.EJE_PPT_CREDITO
                        END)
              ELSE  0 END          
                ) EJECUCION_MES,
        SUM(SALDO_AUX_PPTAL.DISPONIBILIDAD
           ) -  SUM(SALDO_AUX_PPTAL.REG_CONTRACT 
               + SALDO_AUX_PPTAL.REG_NO_CONTRACT
               + SALDO_AUX_PPTAL.MODIF_REG_CONT
               + SALDO_AUX_PPTAL.MODIF_REG_NOCONT
            ) ABIERTAS
FROM PLAN_PRESUPUESTAL 
INNER JOIN SALDO_AUX_PPTAL
  ON PLAN_PRESUPUESTAL.COMPANIA = SALDO_AUX_PPTAL.COMPANIA 
 AND PLAN_PRESUPUESTAL.ANO      = SALDO_AUX_PPTAL.ANO
 AND PLAN_PRESUPUESTAL.CODIGO   = SALDO_AUX_PPTAL.CODIGO
WHERE PLAN_PRESUPUESTAL.COMPANIA     = s$compania$s
  AND PLAN_PRESUPUESTAL.ANO          = s$anio$s
  AND PLAN_PRESUPUESTAL.NATURALEZA   = 'D'
  AND PLAN_PRESUPUESTAL.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  AND FUENTE_RECURSO BETWEEN  's$fuenteInicial$s' AND  's$fuenteFinal$s'
  AND SALDO_AUX_PPTAL.MES <= s$mesFinal$s
GROUP BY PLAN_PRESUPUESTAL.COMPANIA,
         PLAN_PRESUPUESTAL.ANO,
         SALDO_AUX_PPTAL.ID
) SALDOS
 ON V_PLAN_PRESUPUESTAL.COMPANIA = SALDOS.COMPANIA
AND V_PLAN_PRESUPUESTAL.ANO      = SALDOS.ANO
AND V_PLAN_PRESUPUESTAL.ID       = SUBSTR(SALDOS.ID, 1, LENGTH(V_PLAN_PRESUPUESTAL.ID))
GROUP BY V_PLAN_PRESUPUESTAL.ID,
         SUBSTR(V_PLAN_PRESUPUESTAL.CODIGO,0,1),
         V_PLAN_PRESUPUESTAL.CODIGO, 
         V_PLAN_PRESUPUESTAL.NOMBRE,
         V_PLAN_PRESUPUESTAL.AUXILIAR, 
         V_PLAN_PRESUPUESTAL.CENTRO_COSTO, 
         V_PLAN_PRESUPUESTAL.FUENTE_RECURSO,
         FUENTE_RECURSOS.NOMBRE