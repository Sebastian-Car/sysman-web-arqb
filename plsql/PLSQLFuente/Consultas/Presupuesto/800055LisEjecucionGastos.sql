WITH EJECUCIONGASTOS_GUA AS (
  SELECT  LPAD(V_RESUMENPPTO_BASE.CODIGO,1) AS CLASE,
          V_RESUMENPPTO_BASE.COMPANIA,
          V_RESUMENPPTO_BASE.ANO,
          V_RESUMENPPTO_BASE.ID,
          SUM(V_RESUMENPPTO_BASE.APROPIADO			)  APROPIACIONANTERIOR,
          SUM(V_RESUMENPPTO_BASE.TRASLADO_DEBITO	)  TDEBITOA,
          SUM(V_RESUMENPPTO_BASE.TRASLADO_CREDITO	)  TCREDITOA,
          SUM(V_RESUMENPPTO_BASE.APLAZAMIENTO *-1	)  APLAZAMIENTOA,
          SUM(V_RESUMENPPTO_BASE.REDUCCION*-1		)  REDUCCIONA,
          SUM(V_RESUMENPPTO_BASE.ADICION			)  ADICIONA,
          SUM(V_RESUMENPPTO_BASE.DISPONIBILIDAD		)  DISPONIBILIDADA,
          SUM(V_RESUMENPPTO_BASE.COMPROMISOSACUM	)  COMPROMISOA,
          SUM(V_RESUMENPPTO_BASE.OBLIGACIONESACUM	)  OBLIGACIONESA,
          SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT		)  PAGOSA,
          SUM(V_RESUMENPPTO_BASE.APLAZAM_CREDITO	)  APLAZAMIENTOANTERIOR,
          SUM(V_RESUMENPPTO_BASE.APLAZAM_DEBITO		)  DESAPLAZAMIENTOANTERIOR,
          V_RESUMENPPTO_BASE.CONSITUACIONFONDOS,
          V_RESUMENPPTO_BASE.DESTINO
  FROM V_RESUMENPPTO_BASE 
   LEFT JOIN FUENTE_RECURSOS 
    ON V_RESUMENPPTO_BASE.COMPANIA          = FUENTE_RECURSOS.COMPANIA  
    AND V_RESUMENPPTO_BASE.ANO              = FUENTE_RECURSOS.ANO 
    AND V_RESUMENPPTO_BASE.FUENTE_RECURSO   = FUENTE_RECURSOS.CODIGO   
  WHERE V_RESUMENPPTO_BASE.COMPANIA  = s$compania$s
    AND V_RESUMENPPTO_BASE.ANO       = s$ano$s
    AND V_RESUMENPPTO_BASE.ID BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
    AND V_RESUMENPPTO_BASE.MES BETWEEN 0 AND  s$mesFinal$s -1
    AND V_RESUMENPPTO_BASE.NATURALEZA = 'D'  
    AND V_RESUMENPPTO_BASE.TIPOVIGENCIA IN ('VF','VA')
    AND V_RESUMENPPTO_BASE.AUXILIAR IS NULL
    AND V_RESUMENPPTO_BASE.CENTRO_COSTO IS NULL
  GROUP BY
    V_RESUMENPPTO_BASE.COMPANIA,
    V_RESUMENPPTO_BASE.ANO,
    V_RESUMENPPTO_BASE.CODIGO,   
    V_RESUMENPPTO_BASE.ID,
    V_RESUMENPPTO_BASE.TIPOVIGENCIA,
    V_RESUMENPPTO_BASE.CONSITUACIONFONDOS,
    V_RESUMENPPTO_BASE.DESTINO,
    V_RESUMENPPTO_BASE.FUENTE_RECURSO)
SELECT 
  V_RESUMENPPTO_BASE.CODIGO,
  V_RESUMENPPTO_BASE.NOMBRE,
  TO_CHAR(V_RESUMENPPTO_BASE.MOVIMIENTO)MOVIMIENTO,  
  EJECUCIONGASTOS_GUA.APROPIACIONANTERIOR PRESUPUESTO_INICIAL,
  V_RESUMENPPTO_BASE.APROPIADO,
  EJECUCIONGASTOS_GUA.ADICIONA, 
  V_RESUMENPPTO_BASE.ADICION,
  EJECUCIONGASTOS_GUA.ADICIONA + V_RESUMENPPTO_BASE.ADICION TOTALADICION,
  EJECUCIONGASTOS_GUA.REDUCCIONA,
  V_RESUMENPPTO_BASE.REDUCCION *-1 REDUCCION,
  EJECUCIONGASTOS_GUA.REDUCCIONA 	+ V_RESUMENPPTO_BASE.REDUCCION*-1 TOTALREDUCCION,
  EJECUCIONGASTOS_GUA.TDEBITOA CREDITOANTERIOR,
  V_RESUMENPPTO_BASE.TRASLADO_DEBITO CREDITO,
  EJECUCIONGASTOS_GUA.TDEBITOA 		+ V_RESUMENPPTO_BASE.TRASLADO_DEBITO TOTALCREDITOS,
  EJECUCIONGASTOS_GUA.TCREDITOA CONTRACREDITO_ANTERIOR,
  V_RESUMENPPTO_BASE.TRASLADO_CREDITO CONTRACREDITO,
  EJECUCIONGASTOS_GUA.TCREDITOA 	+ V_RESUMENPPTO_BASE.TRASLADO_CREDITO TOTALCONTRACREDITO,
  EJECUCIONGASTOS_GUA.APLAZAMIENTOANTERIOR APLAZAMIENTO_ANTERIOR,
  V_RESUMENPPTO_BASE.APLAZAM_CREDITO APLAZAMIENTO_MES,
  EJECUCIONGASTOS_GUA.APLAZAMIENTOANTERIOR + V_RESUMENPPTO_BASE.APLAZAM_CREDITO TOTAL_APLAZAMIENTOS,
  EJECUCIONGASTOS_GUA.DESAPLAZAMIENTOANTERIOR  DESAPLAZAMIENTO_ANTERIOR,
  V_RESUMENPPTO_BASE.APLAZAM_DEBITO DESAPLAZAMIENTOMES,
  EJECUCIONGASTOS_GUA.DESAPLAZAMIENTOANTERIOR + V_RESUMENPPTO_BASE.APLAZAM_DEBITO TOTAL_DESAPLAZAMIENTOS,
  EJECUCIONGASTOS_GUA.APROPIACIONANTERIOR +
 (EJECUCIONGASTOS_GUA.TDEBITOA 		+ V_RESUMENPPTO_BASE.TRASLADO_DEBITO)-
 (EJECUCIONGASTOS_GUA.TCREDITOA 	+ V_RESUMENPPTO_BASE.TRASLADO_CREDITO)- 
 (EJECUCIONGASTOS_GUA.REDUCCIONA 	+ V_RESUMENPPTO_BASE.REDUCCION*-1)+
 (EJECUCIONGASTOS_GUA.ADICIONA 		+ V_RESUMENPPTO_BASE.ADICION) DEFINITIVO,
  EJECUCIONGASTOS_GUA.DISPONIBILIDADA CDP_ANTERIOR,
  V_RESUMENPPTO_BASE.DISPONIBILIDAD  DISPONIBILIDAD,
  EJECUCIONGASTOS_GUA.DISPONIBILIDADA + V_RESUMENPPTO_BASE.DISPONIBILIDAD TOTAL_CDP,
 (EJECUCIONGASTOS_GUA.DISPONIBILIDADA + V_RESUMENPPTO_BASE.DISPONIBILIDAD)-
 (EJECUCIONGASTOS_GUA.COMPROMISOA  + V_RESUMENPPTO_BASE.COMPROMISOSACUM) DISPONIBILIDADES_ABIERTAS,
  EJECUCIONGASTOS_GUA.COMPROMISOA  REG_COMPROMISO_ANT,
  V_RESUMENPPTO_BASE.COMPROMISOSACUM REG_COMPROMISO_MES,
  EJECUCIONGASTOS_GUA.COMPROMISOA  + V_RESUMENPPTO_BASE.COMPROMISOSACUM REG_COMPROMISO_TOTAL,
 (EJECUCIONGASTOS_GUA.COMPROMISOA  + V_RESUMENPPTO_BASE.COMPROMISOSACUM)- 
 (EJECUCIONGASTOS_GUA.OBLIGACIONESA + V_RESUMENPPTO_BASE.OBLIGACIONESACUM) COMPROMISOS_ABIERTOS,
  EJECUCIONGASTOS_GUA.OBLIGACIONESA REG_OBLIGACION_ANT ,
  V_RESUMENPPTO_BASE.OBLIGACIONESACUM REG_OBLIGACION_MES,
  EJECUCIONGASTOS_GUA.OBLIGACIONESA + V_RESUMENPPTO_BASE.OBLIGACIONESACUM REG_OBLIGACION_TOTAL ,
 (EJECUCIONGASTOS_GUA.OBLIGACIONESA + V_RESUMENPPTO_BASE.OBLIGACIONESACUM)  - 
 (EJECUCIONGASTOS_GUA.PAGOSA + V_RESUMENPPTO_BASE.EJECUCIONPPT) REG_OBLIGACION_ABIERTAS,
  EJECUCIONGASTOS_GUA.PAGOSA PAGOS_ANTERIOR,
  V_RESUMENPPTO_BASE.EJECUCIONPPT PAGOS_MES,
  EJECUCIONGASTOS_GUA.PAGOSA + V_RESUMENPPTO_BASE.EJECUCIONPPT TOTAL_PAGOS,
 (EJECUCIONGASTOS_GUA.OBLIGACIONESA + V_RESUMENPPTO_BASE.OBLIGACIONESACUM) - 
 (EJECUCIONGASTOS_GUA.PAGOSA + V_RESUMENPPTO_BASE.EJECUCIONPPT )SALDO_POR_PAGAR,
 (EJECUCIONGASTOS_GUA.APROPIACIONANTERIOR +
 (EJECUCIONGASTOS_GUA.TDEBITOA + V_RESUMENPPTO_BASE.TRASLADO_DEBITO)-
 (EJECUCIONGASTOS_GUA.TCREDITOA + V_RESUMENPPTO_BASE.TRASLADO_CREDITO)- 
 (EJECUCIONGASTOS_GUA.REDUCCIONA + V_RESUMENPPTO_BASE.REDUCCION*-1)+
 (EJECUCIONGASTOS_GUA.ADICIONA + V_RESUMENPPTO_BASE.ADICION)-
 (EJECUCIONGASTOS_GUA.COMPROMISOA  + V_RESUMENPPTO_BASE.COMPROMISOSACUM)) SALDO_POR_COMPROMETER,
 FUENTE_RECURSOS.NOMBRE NOMBREFUENTE,
 EJECUCIONGASTOS_GUA.CONSITUACIONFONDOS,
 CASE WHEN EJECUCIONGASTOS_GUA.DESTINO ='F' 
   THEN 'FUNCIONAMIENTO'
   ELSE CASE WHEN EJECUCIONGASTOS_GUA.DESTINO = 'I' THEN 'INVERSION' ELSE '' END
 END AS DESTINO
FROM EJECUCIONGASTOS_GUA
  INNER JOIN V_RESUMENPPTO_BASE
    ON EJECUCIONGASTOS_GUA.COMPANIA          = V_RESUMENPPTO_BASE.COMPANIA 
    AND EJECUCIONGASTOS_GUA.ANO              = V_RESUMENPPTO_BASE.ANO    
    AND EJECUCIONGASTOS_GUA.ID               = V_RESUMENPPTO_BASE.ID
 LEFT JOIN FUENTE_RECURSOS 
    ON V_RESUMENPPTO_BASE.COMPANIA          = FUENTE_RECURSOS.COMPANIA  
    AND V_RESUMENPPTO_BASE.ANO              = FUENTE_RECURSOS.ANO 
    AND V_RESUMENPPTO_BASE.FUENTE_RECURSO   = FUENTE_RECURSOS.CODIGO    
WHERE V_RESUMENPPTO_BASE.MES   = s$mesFinal$s
 
ORDER BY V_RESUMENPPTO_BASE.CODIGO
