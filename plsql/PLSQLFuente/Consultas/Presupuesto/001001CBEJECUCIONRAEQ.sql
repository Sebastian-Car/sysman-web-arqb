SELECT SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,
  SALDO_PLAN_PPTAL.CODIGO CUENTA,
  PLAN_PRESUPUESTAL.NOMBRE,
  SUM(APROPIACION_DEBITO-APROPIACION_CREDITO) AS APROPIACIONINICIALF,

  (SUM(SALDO_PLAN_PPTAL.ADICION) + SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO) + SUM(SALDO_PLAN_PPTAL.REDUCCION)) - SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO) CANCELACIONESF,
  (SUM(APROPIACION_DEBITO - APROPIACION_CREDITO) + SUM(SALDO_PLAN_PPTAL.ADICION) + SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO) + SUM(SALDO_PLAN_PPTAL.REDUCCION)) - SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO) RESERVADEFINITIVAF,
  SUM(EJE_PPT_DEBITO - EJE_PPT_CREDITO) AS PAGOSACUMF,
  ((SUM(APROPIACION_DEBITO       -APROPIACION_CREDITO) + SUM(SALDO_PLAN_PPTAL.ADICION) + SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO) + SUM(SALDO_PLAN_PPTAL.REDUCCION)) - SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO)) - (SUM(EJE_PPT_DEBITO - EJE_PPT_CREDITO)) SALDOAPAGARF,
  PLAN_PRESUPUESTAL.DESTINO,
  PLAN_PRESUPUESTAL.NIVEL1,
  PLAN_PRESUPUESTAL.NIVEL2,
  PLAN_PRESUPUESTAL.NIVEL3,
  PLAN_PRESUPUESTAL.NIVEL4,
  PLAN_PRESUPUESTAL.NIVEL5,
  PLAN_PRESUPUESTAL.NIVEL6,
  PLAN_PRESUPUESTAL.NIVEL7,
  PLAN_PRESUPUESTAL.NIVEL8,
  LPAD(SALDO_PLAN_PPTAL.CODIGO, 1) CLASE
FROM PLAN_PRESUPUESTAL
LEFT JOIN SALDO_PLAN_PPTAL
  ON PLAN_PRESUPUESTAL.COMPANIA		= SALDO_PLAN_PPTAL.COMPANIA
AND PLAN_PRESUPUESTAL.ANO		= SALDO_PLAN_PPTAL.ANO
AND PLAN_PRESUPUESTAL.CODIGO		= SALDO_PLAN_PPTAL.CODIGO
WHERE SALDO_PLAN_PPTAL.COMPANIA 	= s$compania$s
AND SALDO_PLAN_PPTAL.MES BETWEEN 0 AND s$mes$s
AND SALDO_PLAN_PPTAL.ANO           		= s$ano$s
AND PLAN_PRESUPUESTAL.NATURALEZA   	= 'D'
AND PLAN_PRESUPUESTAL.INFORME NOT IN (0)
GROUP BY SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,
  SALDO_PLAN_PPTAL.CODIGO,
  PLAN_PRESUPUESTAL.NOMBRE,
  PLAN_PRESUPUESTAL.MOVIMIENTO,
  PLAN_PRESUPUESTAL.CODIGO_EQUIV,
  PLAN_PRESUPUESTAL.DESTINO,
  PLAN_PRESUPUESTAL.NIVEL1,
  PLAN_PRESUPUESTAL.NIVEL2,
  PLAN_PRESUPUESTAL.NIVEL3,
  PLAN_PRESUPUESTAL.NIVEL4,
  PLAN_PRESUPUESTAL.NIVEL5,
  PLAN_PRESUPUESTAL.NIVEL6,
  PLAN_PRESUPUESTAL.NIVEL7,
  PLAN_PRESUPUESTAL.TIPOVIGENCIA,
  LENGTH(SALDO_PLAN_PPTAL.CODIGO),
  PLAN_PRESUPUESTAL.NIVEL8,
  PLAN_PRESUPUESTAL.NATURALEZA
HAVING SALDO_PLAN_PPTAL.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
AND PLAN_PRESUPUESTAL.TIPOVIGENCIA IN ('RA')
AND LENGTH(SALDO_PLAN_PPTAL.CODIGO) BETWEEN 0 AND 99
ORDER BY SALDO_PLAN_PPTAL.CODIGO
