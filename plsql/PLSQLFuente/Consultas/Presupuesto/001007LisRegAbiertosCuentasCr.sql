WITH DETREO AS
  (SELECT NVL(SUM(VALOR_DEBITO-VALOR_CREDITO+MODIFICACION_DEBITO-MODIFICACION_CREDITO),0) VLR_REO,
    DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT,
    DETALLE_COMPROBANTE_PPTAL.ID,
    DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO,
    DETALLE_COMPROBANTE_PPTAL.COMPANIA,
    DETALLE_COMPROBANTE_PPTAL.ANO
  FROM DETALLE_COMPROBANTE_PPTAL
  INNER JOIN TIPO_COMPROBPP
  ON DETALLE_COMPROBANTE_PPTAL.COMPANIA    = TIPO_COMPROBPP.COMPANIA
  AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE  = TIPO_COMPROBPP.CODIGO
  WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA = s$compania$s
  AND TIPO_COMPROBPP.CLASE NOT            IN('DMR','ADR')
  AND TIPO_CPTE_AFECT                      = 'RES'
  GROUP BY DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT,
    DETALLE_COMPROBANTE_PPTAL.ID,
    DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO,
    DETALLE_COMPROBANTE_PPTAL.COMPANIA,
    DETALLE_COMPROBANTE_PPTAL.ANO
  ),
  DETEGR AS
  (SELECT NVL(SUM(VALOR_DEBITO-VALOR_CREDITO+MODIFICACION_DEBITO-MODIFICACION_CREDITO),0) VLR_EGR,
    DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT,
    DETALLE_COMPROBANTE_PPTAL.ID,
    DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO,
    DETALLE_COMPROBANTE_PPTAL.COMPANIA,
    DETALLE_COMPROBANTE_PPTAL.ANO
  FROM DETALLE_COMPROBANTE_PPTAL
  INNER JOIN TIPO_COMPROBPP
  ON DETALLE_COMPROBANTE_PPTAL.COMPANIA    = TIPO_COMPROBPP.COMPANIA
  AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE  = TIPO_COMPROBPP.CODIGO
  WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA = s$compania$s
  AND TIPO_COMPROBPP.CLASE NOT            IN('ARO','DRO')
  AND TIPO_CPTE_AFECT                      = 'RES'
  GROUP BY DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT,
    DETALLE_COMPROBANTE_PPTAL.ID,
    DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO,
    DETALLE_COMPROBANTE_PPTAL.COMPANIA,
    DETALLE_COMPROBANTE_PPTAL.ANO
  )
SELECT 
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
  MIN(DETALLE_COMPROBANTE_PPTAL.TERCERO) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) TERCERO,
  TO_CHAR(MIN(DETALLE_COMPROBANTE_PPTAL.FECHA) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM),'DD/MM/YYYY') FECHA,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
  DETEGR.VLR_EGR,
  DETREO.VLR_REO,
  SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO+DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO) VLR_RES,
  TERCERO.NOMBRE NOMBRETERCERO,
  COMPROBANTE_PPTAL.TIPO_DOCUMENTO||' - '||COMPROBANTE_PPTAL.NRO_DOCUMENTO DOCUMENTO,
  PLAN_PRESUPUESTAL.NOMBRE PLAN_PPTAL_NOM,
  DETALLE_COMPROBANTE_PPTAL.ID CUENTA,
  MIN(TIPO_COMPROBPP.NOMBRE) KEEP (DENSE_RANK FIRST ORDER BY ROWNUM) NOMBRETIPOCPTE,
  SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO+DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO)-VLR_REO SALDORES,
  VLR_REO-VLR_EGR SALDOREO
FROM DETALLE_COMPROBANTE_PPTAL
INNER JOIN TIPO_COMPROBPP
ON DETALLE_COMPROBANTE_PPTAL.COMPANIA   = TIPO_COMPROBPP.COMPANIA
AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
INNER JOIN DETREO
ON DETALLE_COMPROBANTE_PPTAL.COMPANIA     = DETREO.COMPANIA
AND DETALLE_COMPROBANTE_PPTAL.ANO         = DETREO.ANO
AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = DETREO.TIPO_CPTE_AFECT
AND DETALLE_COMPROBANTE_PPTAL.ID          = DETREO.ID
AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = DETREO.CMPTE_AFECTADO
INNER JOIN DETEGR
ON DETALLE_COMPROBANTE_PPTAL.COMPANIA     = DETEGR.COMPANIA
AND DETALLE_COMPROBANTE_PPTAL.ANO         = DETEGR.ANO
AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = DETEGR.TIPO_CPTE_AFECT
AND DETALLE_COMPROBANTE_PPTAL.ID          = DETEGR.ID
AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = DETEGR.CMPTE_AFECTADO
LEFT JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
AND DETALLE_COMPROBANTE_PPTAL.ANO     = PLAN_PRESUPUESTAL.ANO
AND DETALLE_COMPROBANTE_PPTAL.ID      = PLAN_PRESUPUESTAL.ID
LEFT JOIN TERCERO
ON DETALLE_COMPROBANTE_PPTAL.COMPANIA  = TERCERO.COMPANIA
AND DETALLE_COMPROBANTE_PPTAL.TERCERO  = TERCERO.NIT
AND DETALLE_COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL
LEFT JOIN COMPROBANTE_PPTAL
ON DETALLE_COMPROBANTE_PPTAL.COMPANIA     = COMPROBANTE_PPTAL.COMPANIA
AND DETALLE_COMPROBANTE_PPTAL.ANO         = COMPROBANTE_PPTAL.ANO
AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = COMPROBANTE_PPTAL.TIPO
AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA  = s$compania$s
  AND TIPO_COMPROBPP.CLASE                = 'RES'
  AND DETALLE_COMPROBANTE_PPTAL.CUENTA    BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  AND DETALLE_COMPROBANTE_PPTAL.TERCERO   BETWEEN 's$terceroInicial$s' AND 's$terceroFinal$s'
  AND DETALLE_COMPROBANTE_PPTAL.FECHA     BETWEEN 's$fechaInicial$s' AND 's$fechaFinal$s'
GROUP BY DETALLE_COMPROBANTE_PPTAL.COMPANIA,
  DETALLE_COMPROBANTE_PPTAL.ANO,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
  DETALLE_COMPROBANTE_PPTAL.ID,
  DETREO.VLR_REO,
  PLAN_PRESUPUESTAL.NOMBRE,
  DETEGR.VLR_EGR,
  TERCERO.NOMBRE,
  COMPROBANTE_PPTAL.TIPO_DOCUMENTO||' - '||COMPROBANTE_PPTAL.NRO_DOCUMENTO,
  VLR_REO-VLR_EGR
HAVING SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO+DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO)-DETEGR.VLR_EGR NOT IN (0)
