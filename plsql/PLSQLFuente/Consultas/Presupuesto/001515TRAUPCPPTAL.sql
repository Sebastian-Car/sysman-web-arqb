SELECT 
FECHA_COM,
TIPO_COMPROBANTE_NOM TIPOCOM_NOMBRE,
COMPROBANTE,
TIPO TIPO_CPTE,
FECHA_COM,
TERCERO_COM,
SUCURSAL_COM,
TERCERO_COM_NOM,
DESCRIPCION_DET,
CUENTA,
CUENTANOMBRE,
VALOR_DEBITO AS CREDITO,
VALOR_CREDITO AS  CONTRACREDITO
FROM V_DETALLE_AUXILIAR_PPTAL
WHERE COMPANIA = s$compania$s
AND TIPO = 's$tipoCpte$s'
AND ANO  = s$ano$s
AND COMPROBANTE BETWEEN s$numeroPptoInicial$s AND s$numeroPptoFinal$s
