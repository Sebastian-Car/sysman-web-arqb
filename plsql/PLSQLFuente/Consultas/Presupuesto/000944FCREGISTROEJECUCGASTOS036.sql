SELECT 
   SALDO_PLAN_PPTAL.COMPANIA
  ,SALDO_PLAN_PPTAL.ANO
  ,SALDO_PLAN_PPTAL.ID
  ,PLAN_PRESUPUESTAL.NOMBRE
  ,SUM(CASE WHEN s$enMiles$s NOT IN (0)
            THEN ROUND((APROPIACION_DEBITO               - APROPIACION_CREDITO              ) + 
            	        SALDO_PLAN_PPTAL.ADICION                                              + 
            	        SALDO_PLAN_PPTAL.REDUCCION                                            + 
            	       (SALDO_PLAN_PPTAL.TRASLADO_DEBITO - SALDO_PLAN_PPTAL.TRASLADO_CREDITO) + 
            	       (SALDO_PLAN_PPTAL.APLAZAM_DEBITO  - SALDO_PLAN_PPTAL.APLAZAM_CREDITO )
            	      ,3)/1000
            ELSE (APROPIACION_DEBITO               - APROPIACION_CREDITO              ) + 
                  SALDO_PLAN_PPTAL.ADICION                                              + 
                  SALDO_PLAN_PPTAL.REDUCCION                                            + 
                 (SALDO_PLAN_PPTAL.TRASLADO_DEBITO - SALDO_PLAN_PPTAL.TRASLADO_CREDITO) + 
                 (SALDO_PLAN_PPTAL.APLAZAM_DEBITO  - SALDO_PLAN_PPTAL.APLAZAM_CREDITO )
       END) APROPIACIONVIGENTE
  ,SUM(CASE WHEN SALDO_PLAN_PPTAL.MES = s$mes$s
            THEN CASE WHEN s$enMiles$s NOT IN (0)
                      THEN ROUND(SALDO_PLAN_PPTAL.DISPONIBILIDAD,3)/1000
                      ELSE SALDO_PLAN_PPTAL.DISPONIBILIDAD
                 END
       END) DISPONIBILIDADMES
  ,SUM(CASE WHEN s$enMiles$s NOT IN (0)
            THEN ROUND(SALDO_PLAN_PPTAL.DISPONIBILIDAD,3)/1000
            ELSE SALDO_PLAN_PPTAL.DISPONIBILIDAD
       END) DISPONIBILIDADACUM
  ,SUM(CASE WHEN SALDO_PLAN_PPTAL.MES = s$mes$s
            THEN CASE WHEN s$enMiles$s NOT IN (0) 
                      THEN ROUND(SALDO_PLAN_PPTAL.REG_CONTRACT    + 
                      	         SALDO_PLAN_PPTAL.REG_NO_CONTRACT + 
                      	         SALDO_PLAN_PPTAL.REG_REVERSION   + 
                      	         SALDO_PLAN_PPTAL.MODIF_REG_CONT  + 
                      	         SALDO_PLAN_PPTAL.MODIF_REG_NOCONT
                      	        ,-3)/1000
                      ELSE SALDO_PLAN_PPTAL.REG_CONTRACT    + 
                           SALDO_PLAN_PPTAL.REG_NO_CONTRACT + 
                           SALDO_PLAN_PPTAL.REG_REVERSION   + 
                           SALDO_PLAN_PPTAL.MODIF_REG_CONT  + 
                           SALDO_PLAN_PPTAL.MODIF_REG_NOCONT 
                 END
       END) COMPROMISOSMES
  ,CASE WHEN s$enMiles$s NOT IN (0)
        THEN ROUND(SUM(SALDO_PLAN_PPTAL.REG_CONTRACT    ) + 
        	       SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT ) + 
        	       SUM(SALDO_PLAN_PPTAL.REG_REVERSION   ) + 
        	       SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT  ) + 
        	       SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT)
        	      ,-3)/1000 
        ELSE SUM(SALDO_PLAN_PPTAL.REG_CONTRACT    ) + 
             SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT ) + 
             SUM(SALDO_PLAN_PPTAL.REG_REVERSION   ) + 
             SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT  ) + 
             SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT)
   END COMPROMISOSACUM
  ,SUM(CASE WHEN SALDO_PLAN_PPTAL.MES = s$mes$s
            THEN CASE WHEN s$enMiles$s NOT IN (0) 
                      THEN ROUND(SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION + SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION ,-3)/1000
                      ELSE SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION + SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION  
                 END
       END) OBLIGACIONESMES
  ,CASE WHEN s$enMiles$s NOT IN (0) 
        THEN ROUND(SUM(SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION) + SUM(SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION),-3)/1000
        ELSE SUM(SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION) + SUM(SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION)
   END OBLIGACIONESACUM
  ,SUM(CASE WHEN SALDO_PLAN_PPTAL.MES = s$mes$s
            THEN CASE WHEN s$enMiles$s NOT IN (0) 
                      THEN ROUND((EJE_PPT_DEBITO - EJE_PPT_CREDITO),-3)/1000
                      ELSE (EJE_PPT_DEBITO - EJE_PPT_CREDITO)
                 END
       END) PAGOSMES
  ,CASE WHEN s$enMiles$s NOT IN (0)
        THEN ROUND(SUM(EJE_PPT_DEBITO - EJE_PPT_CREDITO),-3)/1000 
        ELSE SUM(EJE_PPT_DEBITO - EJE_PPT_CREDITO)
   END PAGOSACUM
  ,PLAN_PRESUPUESTAL.DESTINO
  ,PLAN_PRESUPUESTAL.NIVEL1 
  ,PLAN_PRESUPUESTAL.NIVEL2
  ,PLAN_PRESUPUESTAL.NIVEL3
  ,PLAN_PRESUPUESTAL.NIVEL4
  ,PLAN_PRESUPUESTAL.NIVEL5
  ,PLAN_PRESUPUESTAL.NIVEL6
  ,LENGTH(NIVEL1||NIVEL2||NIVEL3||NIVEL4||NIVEL5||NIVEL6) LONGCUENTA
  ,LENGTH(PLAN_PRESUPUESTAL.ID) LENCUENTA
  ,CASE WHEN LENGTH(PLAN_PRESUPUESTAL.ID) >= 3 THEN PLAN_PRESUPUESTAL.NIVEL1 END N1
  ,CASE WHEN LENGTH(PLAN_PRESUPUESTAL.ID) >= 4 THEN PLAN_PRESUPUESTAL.NIVEL2 END N2
  ,CASE WHEN LENGTH(PLAN_PRESUPUESTAL.ID) >= 4 THEN PLAN_PRESUPUESTAL.NIVEL3 END N3
  ,CASE WHEN LENGTH(PLAN_PRESUPUESTAL.ID) >= 5 THEN PLAN_PRESUPUESTAL.NIVEL4 END N4
  ,CASE WHEN LENGTH(PLAN_PRESUPUESTAL.ID) >= 6 THEN PLAN_PRESUPUESTAL.NIVEL5 END N5
  ,PLAN_PRESUPUESTAL.NIVEL6 N6
  ,NIVEL1||NIVEL2||NIVEL3||NIVEL4||NIVEL5||NIVEL6 EQUIVALENTE
FROM V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
  INNER JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
     ON SALDO_PLAN_PPTAL.COMPANIA   = PLAN_PRESUPUESTAL.COMPANIA
    AND SALDO_PLAN_PPTAL.ANO        = PLAN_PRESUPUESTAL.ANO
    AND SALDO_PLAN_PPTAL.ID         = PLAN_PRESUPUESTAL.ID
WHERE SALDO_PLAN_PPTAL.COMPANIA = s$compania$s
  AND SALDO_PLAN_PPTAL.ANO      = s$anio$s
  AND PLAN_PRESUPUESTAL.CODIGO     BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'  
  AND SALDO_PLAN_PPTAL.MES         BETWEEN 0                   AND s$mes$s
  AND LENGTH(SALDO_PLAN_PPTAL.ID)  BETWEEN 0                   AND s$nivel$s
  AND LENGTH(PLAN_PRESUPUESTAL.ID) BETWEEN 1                   AND NVL(s$maxDigitos$s,16)
  AND PLAN_PRESUPUESTAL.NATURALEZA IN('D')
  s$condicion$s
  AND LENGTH(NIVEL1||NIVEL2||NIVEL3||NIVEL4||NIVEL5||NIVEL6) <= s$nivel$s + 1 
  s$condInforme$s
GROUP BY 
   SUBSTR(SALDO_PLAN_PPTAL.ID, 1, 1)
  ,SALDO_PLAN_PPTAL.COMPANIA
  ,SALDO_PLAN_PPTAL.ANO
  ,SALDO_PLAN_PPTAL.ID
  ,PLAN_PRESUPUESTAL.ID
  ,PLAN_PRESUPUESTAL.NOMBRE
  ,PLAN_PRESUPUESTAL.DESTINO
  ,PLAN_PRESUPUESTAL.NIVEL1
  ,PLAN_PRESUPUESTAL.NIVEL2
  ,PLAN_PRESUPUESTAL.NIVEL3
  ,PLAN_PRESUPUESTAL.NIVEL4
  ,PLAN_PRESUPUESTAL.NIVEL5
  ,PLAN_PRESUPUESTAL.NIVEL6
  ,NIVEL1||NIVEL2||NIVEL3||NIVEL4||NIVEL5||NIVEL6 
