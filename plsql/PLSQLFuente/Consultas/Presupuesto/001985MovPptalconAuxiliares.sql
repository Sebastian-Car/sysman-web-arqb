SELECT DETALLE_COMPROBANTE_PPTAL.COMPANIA,
       COMPROBANTE_PPTAL.DEPENDENCIA,
       COMPROBANTE_PPTAL.NRO_DOCUMENTO,
       COMPROBANTE_PPTAL.TIPOCONTRATO
       ||' - '
       ||COMPROBANTE_PPTAL.NUMEROCONTRATO CONTRATO,
       DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO AS FUENTE_RECURSOS,
       DEPENDENCIA.NOMBRE NOMBREDEPENDENCIA,
       DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
       TIPO_COMPROBPP.NOMBRE TIPO_COMPROBPP_NOM ,
       DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
       TO_CHAR(DETALLE_COMPROBANTE_PPTAL.FECHA,'DD/MM/YYYY') FECHA,
       CASE
         WHEN 's$tipoCuenta$s' = 1
         THEN 'CODIGO CUENTA'
         ELSE 'FID-UNE-PROG-SPR-PROY-DIS-REC'
       END TITULOCUENTA,
       DETALLE_COMPROBANTE_PPTAL.DESCRIPCION,
       SUM((DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO - DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO)) MODIFICACION,
       SUM((DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO     - DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO)) AFECTACION,
       SUM((DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO    - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) -(DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO - DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO) +(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO - DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO)) SALDO,
       CASE
         WHEN 's$tipoCuenta$s' = 1
         THEN DETALLE_COMPROBANTE_PPTAL.CUENTA
         ELSE LPAD(NVL(PLAN_PRESUPUESTAL.NIVEL1, ''), 3)
           || '-'
           || LPAD(NVL(PLAN_PRESUPUESTAL.NIVEL2, ''), 3)
           || '-'
           || LPAD(NVL(PLAN_PRESUPUESTAL.NIVEL3, ''), 3)
           || '-'
           || LPAD(NVL(PLAN_PRESUPUESTAL.NIVEL4, ''), 3)
           || '-'
           || LPAD(NVL(PLAN_PRESUPUESTAL.NIVEL5, ''), 3)
           || '-'
           || LPAD(NVL(PLAN_PRESUPUESTAL.NIVEL6, ''), 3)
           || '-'
           || LPAD(NVL(PLAN_PRESUPUESTAL.NIVEL7, ''), 3)
       END RUBRO,
       PLAN_PRESUPUESTAL.NOMBRE NOMBREPLAN,
       DETALLE_COMPROBANTE_PPTAL.CUENTA CODIGO_CUENTA,
       DETALLE_COMPROBANTE_PPTAL.ANO,
       TIPO_COMPROBPP.CLASE,
       DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO,
       DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO,
       DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO,
       DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO,
       DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO,
       DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO,
       DETALLE_COMPROBANTE_PPTAL.TERCERO,
       DETALLE_COMPROBANTE_PPTAL.SUCURSAL CODSUCURSAL,
       TERCERO.NOMBRE         AS NOMBRE_TERCERO,
       CENTRO_COSTO.NOMBRE    AS NOMBRE_CENTRO_COSTO,
       AUXILIAR.NOMBRE        AS NOMBRE_AUXILIAR,
       FUENTE_RECURSOS.NOMBRE AS NOMBRE_FUENTE_RECURSOS,
       REFERENCIA.NOMBRE      AS NOMBRE_REFERENCIA ,
       DETALLE_COMPROBANTE_PPTAL.AUXILIAR,
       DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO,
       DETALLE_COMPROBANTE_PPTAL.REFERENCIA,
       (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) MOVIMIENTO
  FROM DETALLE_COMPROBANTE_PPTAL
 INNER JOIN TERCERO
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA  = TERCERO.COMPANIA
   AND DETALLE_COMPROBANTE_PPTAL.TERCERO  = TERCERO.NIT
   AND DETALLE_COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL
 INNER JOIN CENTRO_COSTO
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA      = CENTRO_COSTO.COMPANIA
   AND DETALLE_COMPROBANTE_PPTAL.ANO          = CENTRO_COSTO.ANO
   AND DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO = CENTRO_COSTO.CODIGO
 INNER JOIN AUXILIAR
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA  = AUXILIAR.COMPANIA
   AND DETALLE_COMPROBANTE_PPTAL.ANO      = AUXILIAR.ANO
   AND DETALLE_COMPROBANTE_PPTAL.AUXILIAR = AUXILIAR.CODIGO
 INNER JOIN FUENTE_RECURSOS
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA        = FUENTE_RECURSOS.COMPANIA
   AND DETALLE_COMPROBANTE_PPTAL.ANO            = FUENTE_RECURSOS.ANO
   AND DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO = FUENTE_RECURSOS.CODIGO
 INNER JOIN REFERENCIA
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA    = REFERENCIA.COMPANIA
   AND DETALLE_COMPROBANTE_PPTAL.ANO        = REFERENCIA.ANO
   AND DETALLE_COMPROBANTE_PPTAL.REFERENCIA = REFERENCIA.CODIGO
 INNER JOIN PLAN_PRESUPUESTAL
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
   AND DETALLE_COMPROBANTE_PPTAL.ANO     = PLAN_PRESUPUESTAL.ANO
   AND DETALLE_COMPROBANTE_PPTAL.CUENTA  = PLAN_PRESUPUESTAL.CODIGO
 INNER JOIN TIPO_COMPROBPP
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA   = TIPO_COMPROBPP.COMPANIA
   AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
 INNER JOIN COMPROBANTE_PPTAL
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA     = COMPROBANTE_PPTAL.COMPANIA
   AND DETALLE_COMPROBANTE_PPTAL.ANO         = COMPROBANTE_PPTAL.ANO
   AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO
   AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = COMPROBANTE_PPTAL.TIPO
  LEFT JOIN DEPENDENCIA
    ON COMPROBANTE_PPTAL.COMPANIA            = DEPENDENCIA.COMPANIA
   AND COMPROBANTE_PPTAL.DEPENDENCIA        = DEPENDENCIA.CODIGO
 WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA = s$compania$s
   AND TIPO_COMPROBPP.CODIGO  BETWEEN 's$comprobanteInicial$s' AND 's$comprobanteFinal$s'
   AND TRUNC(DETALLE_COMPROBANTE_PPTAL.FECHA)   BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY')
   AND DETALLE_COMPROBANTE_PPTAL.TERCERO        BETWEEN CASE WHEN 0 <> s$manTer$s THEN 's$terceroInicial$s'    ELSE DETALLE_COMPROBANTE_PPTAL.TERCERO        END AND CASE WHEN 0 <> s$manTer$s THEN 's$terceroFinal$s'    ELSE DETALLE_COMPROBANTE_PPTAL.TERCERO        END  
   AND DETALLE_COMPROBANTE_PPTAL.AUXILIAR       BETWEEN CASE WHEN 0 <> s$manAux$s THEN 's$auxiliarInicial$s'   ELSE DETALLE_COMPROBANTE_PPTAL.AUXILIAR       END AND CASE WHEN 0 <> s$manAux$s THEN 's$auxiliarFinal$s'   ELSE DETALLE_COMPROBANTE_PPTAL.AUXILIAR       END
   AND DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO   BETWEEN CASE WHEN 0 <> s$manCen$s THEN 's$centroInicial$s'     ELSE DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO   END AND CASE WHEN 0 <> s$manCen$s THEN 's$centroFinal$s'     ELSE DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO   END
   AND DETALLE_COMPROBANTE_PPTAL.REFERENCIA     BETWEEN CASE WHEN 0 <> s$manRef$s THEN 's$referenciaInicial$s' ELSE DETALLE_COMPROBANTE_PPTAL.REFERENCIA     END AND CASE WHEN 0 <> s$manRef$s THEN 's$referenciaFinal$s' ELSE DETALLE_COMPROBANTE_PPTAL.REFERENCIA     END
   AND DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO BETWEEN CASE WHEN 0 <> s$manFue$s THEN 's$fuenteInicial$s'     ELSE DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO END AND CASE WHEN 0 <> s$manFue$s THEN 's$fuenteFinal$s'     ELSE DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO END
 GROUP BY DETALLE_COMPROBANTE_PPTAL.COMPANIA,
       COMPROBANTE_PPTAL.DEPENDENCIA,
       COMPROBANTE_PPTAL.NRO_DOCUMENTO,
       COMPROBANTE_PPTAL.TIPOCONTRATO
       ||' - '
       ||COMPROBANTE_PPTAL.NUMEROCONTRATO ,
       DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO ,
       DEPENDENCIA.NOMBRE ,
       DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
       TIPO_COMPROBPP.NOMBRE ,
       DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
       TO_CHAR(DETALLE_COMPROBANTE_PPTAL.FECHA,'DD/MM/YYYY') ,
       CASE
         WHEN 's$tipoCuenta$s' = 1
         THEN 'CODIGO CUENTA'
         ELSE 'FID-UNE-PROG-SPR-PROY-DIS-REC'
       END ,
       DETALLE_COMPROBANTE_PPTAL.DESCRIPCION,
       CASE
         WHEN 's$tipoCuenta$s' = 1
         THEN DETALLE_COMPROBANTE_PPTAL.CUENTA
         ELSE LPAD(NVL(PLAN_PRESUPUESTAL.NIVEL1, ''), 3)
           || '-'
           || LPAD(NVL(PLAN_PRESUPUESTAL.NIVEL2, ''), 3)
           || '-'
           || LPAD(NVL(PLAN_PRESUPUESTAL.NIVEL3, ''), 3)
           || '-'
           || LPAD(NVL(PLAN_PRESUPUESTAL.NIVEL4, ''), 3)
           || '-'
           || LPAD(NVL(PLAN_PRESUPUESTAL.NIVEL5, ''), 3)
           || '-'
           || LPAD(NVL(PLAN_PRESUPUESTAL.NIVEL6, ''), 3)
           || '-'
           || LPAD(NVL(PLAN_PRESUPUESTAL.NIVEL7, ''), 3)
       END ,
       PLAN_PRESUPUESTAL.NOMBRE ,
       DETALLE_COMPROBANTE_PPTAL.CUENTA ,
       DETALLE_COMPROBANTE_PPTAL.ANO,
       TIPO_COMPROBPP.CLASE,
       DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO,
       DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO,
       DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO,
       DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO,
       DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO,
       DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO,
       DETALLE_COMPROBANTE_PPTAL.TERCERO,
       DETALLE_COMPROBANTE_PPTAL.SUCURSAL ,
       TERCERO.NOMBRE ,
       CENTRO_COSTO.NOMBRE ,
       AUXILIAR.NOMBRE ,
       FUENTE_RECURSOS.NOMBRE ,
       REFERENCIA.NOMBRE ,
       DETALLE_COMPROBANTE_PPTAL.AUXILIAR,
       DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO,
       DETALLE_COMPROBANTE_PPTAL.REFERENCIA,
       (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)
