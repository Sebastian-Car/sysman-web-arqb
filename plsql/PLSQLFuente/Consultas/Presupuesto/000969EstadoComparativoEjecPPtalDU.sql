WITH RESUMENPPTOP_INFORMES AS
  (SELECT SALDO_PLAN_PPTAL.COMPANIA,
          SALDO_PLAN_PPTAL.ANO,
          PLAN_PRESUPUESTAL.ID,
          SUM(SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION)+SUM(SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION) TOTALREO,
          SUM(
          CASE
            WHEN PLAN_PRESUPUESTAL.NATURALEZA ='D'
            THEN APROPIACION_DEBITO -APROPIACION_CREDITO
            ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
          END)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.REDUCCION)+SUM(
          CASE
            WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
            THEN APLAZAM_DEBITO -APLAZAM_CREDITO
            ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
          END)+SUM(
          CASE
            WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
            THEN TRASLADO_DEBITO -TRASLADO_CREDITO
            ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
          END) APRDEFINITIVA,
          SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD) DISPONIBILIDAD,
          CASE
            WHEN PLAN_PRESUPUESTAL.NATURALEZA='C'
            THEN SUM(REG_CONTRACT   +MODIF_REG_CONT)
            ELSE SUM(REG_NO_CONTRACT+MODIF_REG_NOCONT)
          END REGISTROSTOTAL,
          SUM(SALDO_PLAN_PPTAL.ADICION) ADICION,
          SUM(SALDO_PLAN_PPTAL.REDUCCION) REDUCCION,
          SUM(
          CASE
            WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
            THEN TRASLADO_DEBITO -TRASLADO_CREDITO
            ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
          END) TRASLADO,
          SUM(
          CASE
            WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
            THEN APLAZAM_DEBITO -APLAZAM_CREDITO
            ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
          END) APLAZAMIENTO,
          PLAN_PRESUPUESTAL.NOMBRE
  FROM V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
   INNER JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
    ON SALDO_PLAN_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
    AND SALDO_PLAN_PPTAL.ANO     = PLAN_PRESUPUESTAL.ANO
    AND SALDO_PLAN_PPTAL.ID  = PLAN_PRESUPUESTAL.ID
  WHERE SALDO_PLAN_PPTAL.COMPANIA    = s$compania$s
    AND SALDO_PLAN_PPTAL.ANO         = s$anio$s
    AND PLAN_PRESUPUESTAL.NATURALEZA = 'D' 
    AND LENGTH(SALDO_PLAN_PPTAL.ID)               <= s$nivel$s
    AND SALDO_PLAN_PPTAL.MES         BETWEEN s$mesInicial$s AND s$mesFinal$s
    AND PLAN_PRESUPUESTAL.CODIGO     BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  GROUP BY SALDO_PLAN_PPTAL.COMPANIA,
           SALDO_PLAN_PPTAL.ANO,
           PLAN_PRESUPUESTAL.ID,
           PLAN_PRESUPUESTAL.NOMBRE,
           SALDO_PLAN_PPTAL.ID,
           PLAN_PRESUPUESTAL.CODIGO,
           PLAN_PRESUPUESTAL.TIPOVIGENCIA,
           PLAN_PRESUPUESTAL.NATURALEZA,
           PLAN_PRESUPUESTAL.CODIGO,
           LENGTH(SALDO_PLAN_PPTAL.ID)
  ),
  RESUMENPPTOA_INFORMES AS
  (SELECT SALDO_PLAN_PPTAL.COMPANIA,
          SALDO_PLAN_PPTAL.ANO,
          PLAN_PRESUPUESTAL.ID,
          SUM(
          CASE
            WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
            THEN APROPIACION_DEBITO -APROPIACION_CREDITO
            ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
          END)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.REDUCCION)+SUM(
          CASE
            WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
            THEN APLAZAM_DEBITO -APLAZAM_CREDITO
            ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
          END)+SUM(
          CASE
            WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
            THEN TRASLADO_DEBITO -TRASLADO_CREDITO
            ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
          END) APRDEFINITIVA,
          SUM(SALDO_PLAN_PPTAL.REDUCCION) REDUCCION
  FROM V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
   INNER JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
    ON  SALDO_PLAN_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
    AND SALDO_PLAN_PPTAL.ANO      = PLAN_PRESUPUESTAL.ANO
    AND SALDO_PLAN_PPTAL.ID       = PLAN_PRESUPUESTAL.ID
  WHERE SALDO_PLAN_PPTAL.COMPANIA    = s$compania$s
    AND SALDO_PLAN_PPTAL.ANO         = s$anio$s
    AND SALDO_PLAN_PPTAL.MES         < s$mesInicial$s
    AND PLAN_PRESUPUESTAL.NATURALEZA = 'D'
    AND LENGTH(SALDO_PLAN_PPTAL.ID)              <= s$nivel$s
    AND PLAN_PRESUPUESTAL.CODIGO     BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  GROUP BY SALDO_PLAN_PPTAL.COMPANIA,
           SALDO_PLAN_PPTAL.ANO,
           PLAN_PRESUPUESTAL.ID,
           LENGTH(SALDO_PLAN_PPTAL.ID),
           PLAN_PRESUPUESTAL.NOMBRE,
           SALDO_PLAN_PPTAL.ID,
           PLAN_PRESUPUESTAL.CODIGO,
           PLAN_PRESUPUESTAL.NATURALEZA,
           PLAN_PRESUPUESTAL.CENTRO_COSTO,
           PLAN_PRESUPUESTAL.AUXILIAR,
           PLAN_PRESUPUESTAL.CODIGO
  )
SELECT RESUMENPPTOP_INFORMES.ID,
       RESUMENPPTOP_INFORMES.NOMBRE,
       RESUMENPPTOP_INFORMES.ADICION+RESUMENPPTOP_INFORMES.REDUCCION+RESUMENPPTOA_INFORMES.REDUCCION+RESUMENPPTOP_INFORMES.APLAZAMIENTO+RESUMENPPTOP_INFORMES.TRASLADO MODIFICACIONES,
       RESUMENPPTOP_INFORMES.TRASLADO,
       RESUMENPPTOP_INFORMES.ADICION,
       RESUMENPPTOP_INFORMES.REDUCCION,
       RESUMENPPTOA_INFORMES.APRDEFINITIVA APROINICIAL,
       RESUMENPPTOP_INFORMES.APRDEFINITIVA APRDEFINITIVAP,
       RESUMENPPTOA_INFORMES.APRDEFINITIVA+RESUMENPPTOP_INFORMES.APRDEFINITIVA APROPDEF,
       RESUMENPPTOP_INFORMES.APLAZAMIENTO,
       RESUMENPPTOP_INFORMES.DISPONIBILIDAD,
       RESUMENPPTOP_INFORMES.REGISTROSTOTAL,
       RESUMENPPTOP_INFORMES.TOTALREO,
       RESUMENPPTOP_INFORMES.DISPONIBILIDAD-RESUMENPPTOP_INFORMES.REGISTROSTOTAL DIFERENCIA,
       RESUMENPPTOA_INFORMES.APRDEFINITIVA +RESUMENPPTOP_INFORMES.APRDEFINITIVA-RESUMENPPTOP_INFORMES.DISPONIBILIDAD SALDO,
       RESUMENPPTOP_INFORMES.REDUCCION,
     PCK_SYSMAN_UTL.FC_ROUND(
       CASE
         WHEN (RESUMENPPTOA_INFORMES.APRDEFINITIVA+RESUMENPPTOP_INFORMES.APRDEFINITIVA) IN (0)
         THEN 0
         ELSE (RESUMENPPTOP_INFORMES.DISPONIBILIDAD/(RESUMENPPTOA_INFORMES.APRDEFINITIVA+RESUMENPPTOP_INFORMES.APRDEFINITIVA))*100
       END ,2) PORCENTAJE 
FROM RESUMENPPTOP_INFORMES
 LEFT JOIN RESUMENPPTOA_INFORMES
  ON  RESUMENPPTOP_INFORMES.COMPANIA = RESUMENPPTOA_INFORMES.COMPANIA
  AND RESUMENPPTOP_INFORMES.ANO      = RESUMENPPTOA_INFORMES.ANO
  AND RESUMENPPTOP_INFORMES.ID       = RESUMENPPTOA_INFORMES.ID
