SELECT DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE, 
       DETALLE_COMPROBANTE_PPTAL.COMPROBANTE, 
       DETALLE_COMPROBANTE_PPTAL.CUENTA CODIGO_CUENTA, 
       DETALLE_COMPROBANTE_PPTAL.FECHA, 
       DETALLE_COMPROBANTE_PPTAL.DESCRIPCION, 
       DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO, 
       DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO, 
       NVL(TIPO_CPTE_AFECT, '')  TIPO_CPTE_AFECTA, 
       DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO, 
       TIPO_COMPROBPP.NOMBRE NOMBRETIPOCOM, 
       TERCERO.NOMBRE NOMBRETERCERO, 
       PLAN_PRESUPUESTAL.NOMBRE NOMBREPLANPRES, 
       CASE WHEN VALOR_DEBITO > 0 THEN VALOR_DEBITO ELSE VALOR_CREDITO END  VALORUNICO, 
       TIPO_COMPROBPP_1.NOMBRE NOMBRETIPOCOM1, 
       DETALLE_COMPROBANTE_PPTAL.COMPANIA 
FROM   DETALLE_COMPROBANTE_PPTAL 
INNER JOIN TIPO_COMPROBPP 
ON     DETALLE_COMPROBANTE_PPTAL.COMPANIA  = TIPO_COMPROBPP.COMPANIA 
AND    DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO  
INNER JOIN TERCERO 
ON     DETALLE_COMPROBANTE_PPTAL.COMPANIA  = TERCERO.COMPANIA   
AND    DETALLE_COMPROBANTE_PPTAL.TERCERO   = TERCERO.NIT 
AND    DETALLE_COMPROBANTE_PPTAL.SUCURSAL  = TERCERO.SUCURSAL 
INNER JOIN PLAN_PRESUPUESTAL 
ON     DETALLE_COMPROBANTE_PPTAL.COMPANIA      = PLAN_PRESUPUESTAL.COMPANIA 
AND    DETALLE_COMPROBANTE_PPTAL.CUENTA    = PLAN_PRESUPUESTAL.CODIGO
AND    DETALLE_COMPROBANTE_PPTAL.ANO           = PLAN_PRESUPUESTAL.ANO
INNER JOIN TIPO_COMPROBPP  TIPO_COMPROBPP_1 
ON DETALLE_COMPROBANTE_PPTAL.COMPANIA         = TIPO_COMPROBPP_1.COMPANIA 
AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT = TIPO_COMPROBPP_1.CODIGO
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA =  s$compania$s
AND   DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = 's$tipo$s'
AND   DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = s$numero$s
AND   NVL(TIPO_CPTE_AFECT, '') IS NOT NULL 
AND   DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO IS NOT NULL 
AND   DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO  NOT IN (0)
ORDER BY 
    DETALLE_COMPROBANTE_PPTAL.COMPANIA, 
    DETALLE_COMPROBANTE_PPTAL.ANO, 
    DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE, 
    DETALLE_COMPROBANTE_PPTAL.COMPROBANTE, 
    DETALLE_COMPROBANTE_PPTAL.CONSECUTIVO
