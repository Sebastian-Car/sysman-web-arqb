SELECT 
s$aplicarMiles$sVS.REG_CONTRACT+VS.REG_NO_CONTRACT+VS.REG_REVERSION+VS.MODIF_REG_CONT+VS.MODIF_REG_NOCONTs$dividir$s AS COMPROMISOSMES,
s$aplicarMiles$sVS.REGISTRO_OBLIGACION+VS.MODIF_REGISTRO_OBLIGACIONs$dividir$s  AS OBLIGACIONESMES,
s$aplicarMiles$sVS.EJE_PPT_DEBITO-VS.EJE_PPT_CREDITOs$dividir$s AS PAGOSMES,
REGISTROEJECUCGASTOS_036.CLASE,
REGISTROEJECUCGASTOS_036.CUENTA,
REGISTROEJECUCGASTOS_036.DESTINO,
REGISTROEJECUCGASTOS_036.NIVEL1,
REGISTROEJECUCGASTOS_036.NIVEL2,
REGISTROEJECUCGASTOS_036.NIVEL3,
REGISTROEJECUCGASTOS_036.NIVEL4,
REGISTROEJECUCGASTOS_036.NIVEL5,
REGISTROEJECUCGASTOS_036.NIVEL6,
REGISTROEJECUCGASTOS_036.NOMBRE,
REGISTROEJECUCGASTOS_036.APROPIACIONVIGENTE,  
REGISTROEJECUCGASTOS_036.COMPROMISOSACUM,
REGISTROEJECUCGASTOS_036.OBLIGACIONESACUM, 
REGISTROEJECUCGASTOS_036.PAGOSACUM,
REGISTROEJECUCGASTOS_036.DISPONIBILIDADACUMU,
VS.DISPONIBILIDAD AS DISPONIBILIDADMES
FROM (SELECT 
SUBSTR(SA.ID,1,1) AS CLASE,
SA.COMPANIA,
  SA.ANO,
  SA.ID CUENTA,
  PR.DESTINO,
  PR.NIVEL1,
  PR.NIVEL2,
  PR.NIVEL3,
  PR.NIVEL4,
  PR.NIVEL5,
  PR.NIVEL6,
  PR.NOMBRE,
  s$aplicarMiles$s(SUM(CASE WHEN PR.NATURALEZA='D' THEN APROPIACION_DEBITO-APROPIACION_CREDITO ELSE APROPIACION_CREDITO-APROPIACION_DEBITO END)+SUM(SA.ADICION)+SUM(SA.TRASLADO_DEBITO)+SUM(SA.REDUCCION)+SUM(SA.APLAZAM_DEBITO -SA.APLAZAM_CREDITO))-(SUM(SA.TRASLADO_CREDITO))s$dividir$s AS APROPIACIONVIGENTE,  
  s$aplicarMiles$sSUM(SA.REG_CONTRACT)+SUM(SA.REG_NO_CONTRACT)+SUM(SA.REG_REVERSION)+SUM(SA.MODIF_REG_CONT)+SUM(SA.MODIF_REG_NOCONT)s$dividir$s AS COMPROMISOSACUM,
  s$aplicarMiles$sSUM(SA.REGISTRO_OBLIGACION)+SUM(SA.MODIF_REGISTRO_OBLIGACION)s$dividir$s AS OBLIGACIONESACUM, 
  s$aplicarMiles$sSUM(SA.EJE_PPT_DEBITO-SA.EJE_PPT_CREDITO)s$dividir$s AS PAGOSACUM,
  s$aplicarMiles$sSUM(SA.DISPONIBILIDAD)s$dividir$s AS DISPONIBILIDADACUMU
FROM V_SALDO_PLAN_PPTAL SA
LEFT JOIN V_PLAN_PRESUPUESTAL PR
ON (SA.ID = PR.ID)
AND (SA.ANO = PR.ANO)
AND (SA.COMPANIA = PR.COMPANIA)
WHERE SA.COMPANIA = s$compania$s
AND SA.MES BETWEEN 0 AND s$mes$s
AND SA.ANO = s$anio$s
AND SA.ID BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
AND LENGTH(SA.ID) <= s$nivelCuenta$s
AND PR.INFORME <> 0
AND PR.NATURALEZA = 'D'
GROUP BY SA.COMPANIA,
  SA.ANO,
  SA.ID,  
  PR.DESTINO,
  PR.NIVEL1,
  PR.NIVEL2,
  PR.NIVEL3,
  PR.NIVEL4,
  PR.NIVEL5,
  PR.NIVEL6,
  PR.NOMBRE) REGISTROEJECUCGASTOS_036
INNER JOIN V_SALDO_PLAN_PPTAL VS
ON (REGISTROEJECUCGASTOS_036.COMPANIA = VS.COMPANIA)
AND (REGISTROEJECUCGASTOS_036.ANO     = VS.ANO)
AND (REGISTROEJECUCGASTOS_036.CUENTA  = VS.ID)
WHERE VS.MES = s$mes$s
ORDER BY REGISTROEJECUCGASTOS_036.CUENTA
