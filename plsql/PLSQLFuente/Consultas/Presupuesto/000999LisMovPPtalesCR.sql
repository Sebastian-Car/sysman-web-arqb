SELECT 
   DETALLE_COMPROBANTE_PPTAL.CONSECUTIVO,
   DETALLE_COMPROBANTE_PPTAL.SUCURSAL, 
   DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
   DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT , 
   DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
   COMPROBANTE_PPTAL.NUMERO,  
   DETALLE_COMPROBANTE_PPTAL.COMPANIA, 
   TERCERO.NOMBRE, 
   TIPO_COMPROBPP.NOMBRE NOMCOMP, 
   TIPO_COMPROBPP.TIPOAFECTA, 
   PLAN_PRESUPUESTAL.NOMBRE NOMBREPLAN , 
   CASE WHEN DETALLE_COMPROBANTE_PPTAL.TERCERO = PCK_DATOS.FC_CONS_TERCERO
       THEN '' 
       ELSE TERCERO_1.NOMBRE
   END NOMBRETERCERO,
  
 COMPROBANTE_PPTAL.DESCRIPCION,  
   DETALLE_COMPROBANTE_PPTAL.TERCERO, 
  CASE WHEN DETALLE_COMPROBANTE_PPTAL.TERCERO = PCK_DATOS.FC_CONS_TERCERO 
       THEN '' 
       ELSE COMPROBANTE_PPTAL.TERCERO
  END CODTERCERO,
  CASE WHEN DETALLE_COMPROBANTE_PPTAL.SUCURSAL =PCK_DATOS.FC_CONS_SUCURSAL
       THEN '' 
       ELSE COMPROBANTE_PPTAL.SUCURSAL
  END CODSUCURSAL,
  DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO, 
  DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO, 
  TERCERO_1.NOMBRE NOMBRE_TERCERO, 
  DETALLE_COMPROBANTE_PPTAL.FECHA,
 
 DETALLE_COMPROBANTE_PPTAL.ID CODIGO_CUENTA, 
  DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO,  
  CASE WHEN TIPO_COMPROBPP.CLASE  NOT IN ('EGR') 
       THEN CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
                 THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)-(DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO)+(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO)
                 ELSE (DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO-DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO)-(DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO)+(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO)
            END
       ELSE 0
  END SALDOPOREJECUTAR,
  CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' 
       THEN (DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO)+(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO)
       ELSE (DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO)+(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO)
  END AFECTACIONES
FROM DETALLE_COMPROBANTE_PPTAL 
  LEFT JOIN TERCERO TERCERO_1
    ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = TERCERO_1.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.TERCERO  = TERCERO_1.NIT
    AND DETALLE_COMPROBANTE_PPTAL.SUCURSAL = TERCERO_1.SUCURSAL
  LEFT JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
    ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO      = PLAN_PRESUPUESTAL.ANO
    AND DETALLE_COMPROBANTE_PPTAL.ID       = PLAN_PRESUPUESTAL.ID
  INNER JOIN COMPROBANTE_PPTAL
    ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA    = COMPROBANTE_PPTAL.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO         = COMPROBANTE_PPTAL.ANO 
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = COMPROBANTE_PPTAL.TIPO 
    AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO 
  LEFT JOIN TERCERO
    ON  COMPROBANTE_PPTAL.COMPANIA  = TERCERO.COMPANIA
    AND COMPROBANTE_PPTAL.TERCERO   = TERCERO.NIT 
    AND COMPROBANTE_PPTAL.SUCURSAL  = TERCERO.SUCURSAL
  LEFT JOIN TIPO_COMPROBPP
    ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA  = TIPO_COMPROBPP.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO 
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA = s$compania$s 
  AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE BETWEEN 's$tipoInicial$s' AND 's$tipoFinal$s'
  AND TRUNC(DETALLE_COMPROBANTE_PPTAL.FECHA) BETWEEN TO_DATE('s$fechaInicial$s') AND TO_DATE('s$fechaFinal$s') 
