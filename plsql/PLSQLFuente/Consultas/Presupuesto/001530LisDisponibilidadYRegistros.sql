WITH LISDISPONIBILIDADYREGISTROS AS
  (SELECT V_DETALLE_AUXILIAR_PPTAL.COMPANIA,
    V_DETALLE_AUXILIAR_PPTAL.FECHA AS FECHADISPTEXTO,
    V_DETALLE_AUXILIAR_PPTAL.TIPO TIPO_CPTE,
    V_DETALLE_AUXILIAR_PPTAL.COMPROBANTE                                                                                                                                        AS NODISP,
    V_DETALLE_AUXILIAR_PPTAL.FECHA                                                                                                                                              AS FECHADISP,
    (V_DETALLE_AUXILIAR_PPTAL.VALOR_DEBITO-V_DETALLE_AUXILIAR_PPTAL.VALOR_CREDITO)+(V_DETALLE_AUXILIAR_PPTAL.MODIFICACION_DEBITO-V_DETALLE_AUXILIAR_PPTAL.MODIFICACION_CREDITO) AS VALORDISP,
    PLAN_PRESUPUESTAL.NOMBRE                                                                                                                                                    AS NOMBRERUBRO,
    V_DETALLE_AUXILIAR_PPTAL.CUENTA                                                                                                                                             AS RUBRO,
    V_DETALLE_AUXILIAR_PPTAL.ANO,
    V_DETALLE_AUXILIAR_PPTAL.CONSECUTIVO
  FROM V_DETALLE_AUXILIAR_PPTAL
  INNER JOIN PLAN_PRESUPUESTAL
  ON V_DETALLE_AUXILIAR_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
  AND V_DETALLE_AUXILIAR_PPTAL.ANO     = PLAN_PRESUPUESTAL.ANO
  AND V_DETALLE_AUXILIAR_PPTAL.CUENTA  = PLAN_PRESUPUESTAL.CODIGO
  INNER JOIN TIPO_COMPROBPP
  ON V_DETALLE_AUXILIAR_PPTAL.COMPANIA   = TIPO_COMPROBPP.COMPANIA
  AND V_DETALLE_AUXILIAR_PPTAL.TIPO      = TIPO_COMPROBPP.CODIGO
  WHERE V_DETALLE_AUXILIAR_PPTAL.COMPANIA='s$compania$s'
  AND V_DETALLE_AUXILIAR_PPTAL.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND (TIPO_COMPROBPP.CLASE) IN ('DIS')
  ),
  LISDISPONIBILIDADYREGISTROSRES AS
  (SELECT V_DETALLE_AUXILIAR_PPTAL.COMPANIA,
    V_DETALLE_AUXILIAR_PPTAL.ANO,
    TIPO_COMPROBPP.CODIGO TIPO_CPTE,
    V_DETALLE_AUXILIAR_PPTAL.TIPO_CPTE_AFECT,
    V_DETALLE_AUXILIAR_PPTAL.CMPTE_AFECTADO,
    V_DETALLE_AUXILIAR_PPTAL.CONSECUTIVOPPTO,
    V_DETALLE_AUXILIAR_PPTAL.TERCERO,
    V_DETALLE_AUXILIAR_PPTAL.TERCERO_COM_NOM   ,
    V_DETALLE_AUXILIAR_PPTAL.SUCURSAL,
    V_DETALLE_AUXILIAR_PPTAL.COMPROBANTE,
    V_DETALLE_AUXILIAR_PPTAL.FECHA,
    V_DETALLE_AUXILIAR_PPTAL.NRO_DOCUMENTO,
    V_DETALLE_AUXILIAR_PPTAL.DESCRIPCION,
    (V_DETALLE_AUXILIAR_PPTAL.VALOR_DEBITO-V_DETALLE_AUXILIAR_PPTAL.VALOR_CREDITO)+(V_DETALLE_AUXILIAR_PPTAL.MODIFICACION_DEBITO-V_DETALLE_AUXILIAR_PPTAL.MODIFICACION_CREDITO) AS VALORRES
  FROM V_DETALLE_AUXILIAR_PPTAL
  INNER JOIN PLAN_PRESUPUESTAL
  ON V_DETALLE_AUXILIAR_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
  AND V_DETALLE_AUXILIAR_PPTAL.ANO     = PLAN_PRESUPUESTAL.ANO
  AND V_DETALLE_AUXILIAR_PPTAL.CUENTA  = PLAN_PRESUPUESTAL.CODIGO
  INNER JOIN TIPO_COMPROBPP
  ON V_DETALLE_AUXILIAR_PPTAL.COMPANIA   = TIPO_COMPROBPP.COMPANIA
  AND V_DETALLE_AUXILIAR_PPTAL.TIPO      = TIPO_COMPROBPP.CODIGO
  WHERE V_DETALLE_AUXILIAR_PPTAL.COMPANIA='s$compania$s'
  AND V_DETALLE_AUXILIAR_PPTAL.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND (TIPO_COMPROBPP.CLASE) IN ('RES')
  )
SELECT LISDISPONIBILIDADYREGISTROS.COMPANIA,
  LISDISPONIBILIDADYREGISTROS.FECHADISPTEXTO,
  LISDISPONIBILIDADYREGISTROS.TIPO_CPTE,
  LISDISPONIBILIDADYREGISTROS.NODISP,
  LISDISPONIBILIDADYREGISTROS.FECHADISP,
  LISDISPONIBILIDADYREGISTROS.VALORDISP,
  LISDISPONIBILIDADYREGISTROS.NOMBRERUBRO,
  LISDISPONIBILIDADYREGISTROS.RUBRO,
  LISDISPONIBILIDADYREGISTROS.ANO,
  LISDISPONIBILIDADYREGISTROS.CONSECUTIVO,
  LISDISPONIBILIDADYREGISTROSRES.VALORRES,
  LISDISPONIBILIDADYREGISTROSRES.FECHA AS FECHARES,
  CASE
    WHEN NVL(LISDISPONIBILIDADYREGISTROSRES.NRO_DOCUMENTO, ' ')<> ' '
    THEN LISDISPONIBILIDADYREGISTROSRES.NRO_DOCUMENTO
    ELSE COMPROBANTE_PPTAL.NRO_DOCUMENTO
  END AS ACTO,
  CASE
    WHEN NVL(LISDISPONIBILIDADYREGISTROSRES.DESCRIPCION, ' ') <> ' '
    THEN LISDISPONIBILIDADYREGISTROSRES.DESCRIPCION
    ELSE COMPROBANTE_PPTAL.DESCRIPCION
  END                                        AS OBJETO,
  LISDISPONIBILIDADYREGISTROSRES.COMPROBANTE AS NUMERORES,
  LISDISPONIBILIDADYREGISTROSRES.TERCERO_COM_NOM                             AS CONTRATISTA
FROM LISDISPONIBILIDADYREGISTROS
LEFT JOIN LISDISPONIBILIDADYREGISTROSRES
ON LISDISPONIBILIDADYREGISTROS.CONSECUTIVO = LISDISPONIBILIDADYREGISTROSRES.CONSECUTIVOPPTO
AND LISDISPONIBILIDADYREGISTROS.NODISP     = LISDISPONIBILIDADYREGISTROSRES.CMPTE_AFECTADO
AND LISDISPONIBILIDADYREGISTROS.TIPO_CPTE  = LISDISPONIBILIDADYREGISTROSRES.TIPO_CPTE_AFECT
AND LISDISPONIBILIDADYREGISTROS.ANO        = LISDISPONIBILIDADYREGISTROSRES.ANO
AND LISDISPONIBILIDADYREGISTROS.COMPANIA   = LISDISPONIBILIDADYREGISTROSRES.COMPANIA
LEFT JOIN COMPROBANTE_PPTAL
ON LISDISPONIBILIDADYREGISTROSRES.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO
AND LISDISPONIBILIDADYREGISTROSRES.TIPO_CPTE  = COMPROBANTE_PPTAL.TIPO
AND LISDISPONIBILIDADYREGISTROSRES.ANO        = COMPROBANTE_PPTAL.ANO
AND LISDISPONIBILIDADYREGISTROSRES.COMPANIA   = COMPROBANTE_PPTAL.COMPANIA
s$ordenamiento$s
