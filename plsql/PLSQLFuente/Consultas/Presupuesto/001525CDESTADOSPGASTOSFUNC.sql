SELECT V_RESUMENPPTO_BASE.COMPANIA,
  V_RESUMENPPTO_BASE.ANO,
  V_RESUMENPPTO_BASE.CODIGO,
  V_RESUMENPPTO_BASE.NOMBRE,
  SUM(V_RESUMENPPTO_BASE.APROPIADO) APROPIADO,
  SUM(V_RESUMENPPTO_BASE.ADICION       + V_RESUMENPPTO_BASE.REDUCCION + V_RESUMENPPTO_BASE.TRASLADO + V_RESUMENPPTO_BASE.APLAZAMIENTO) MODIFICACIONES,
  SUM(V_RESUMENPPTO_BASE.APROPIADO     + V_RESUMENPPTO_BASE.ADICION + V_RESUMENPPTO_BASE.REDUCCION + V_RESUMENPPTO_BASE.TRASLADO + V_RESUMENPPTO_BASE.APLAZAMIENTO) PPTODEFINITIVO,
  SUM(V_RESUMENPPTO_BASE.PAC_APROPIADO + V_RESUMENPPTO_BASE.MODIFPAC) PACTOTAL,
  (SUM(V_RESUMENPPTO_BASE.APROPIADO    + V_RESUMENPPTO_BASE.ADICION + V_RESUMENPPTO_BASE.REDUCCION + V_RESUMENPPTO_BASE.TRASLADO + V_RESUMENPPTO_BASE.APLAZAMIENTO) - SUM(V_RESUMENPPTO_BASE.PAC_APROPIADO + V_RESUMENPPTO_BASE.MODIFPAC)) SALDOPPTOSEGUNPAC,
  (SUM(V_RESUMENPPTO_BASE.REG_CONTRACT + V_RESUMENPPTO_BASE.MODIF_REG_CONT) + SUM(V_RESUMENPPTO_BASE.REG_NO_CONTRACT + V_RESUMENPPTO_BASE.MODIF_REG_NOCONT)) ACUMULADOCOMPROMISOS,
  SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT) EJECUCIONPPT,
  (SUM(V_RESUMENPPTO_BASE.APROPIADO     + V_RESUMENPPTO_BASE.ADICION + V_RESUMENPPTO_BASE.REDUCCION + V_RESUMENPPTO_BASE.TRASLADO + V_RESUMENPPTO_BASE.APLAZAMIENTO) - (SUM(V_RESUMENPPTO_BASE.REG_CONTRACT + V_RESUMENPPTO_BASE.MODIF_REG_CONT)) + (SUM(V_RESUMENPPTO_BASE.REG_NO_CONTRACT + V_RESUMENPPTO_BASE.MODIF_REG_NOCONT))) SALDOPPTONOCAUSADO,
  (SUM(V_RESUMENPPTO_BASE.APROPIADO     + V_RESUMENPPTO_BASE.ADICION + V_RESUMENPPTO_BASE.REDUCCION + V_RESUMENPPTO_BASE.TRASLADO + V_RESUMENPPTO_BASE.APLAZAMIENTO)) - SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT) SALDOPPTOEFECTIVO,
  (SUM(V_RESUMENPPTO_BASE.PAC_APROPIADO + V_RESUMENPPTO_BASE.MODIFPAC)) - (SUM(V_RESUMENPPTO_BASE.REG_CONTRACT + V_RESUMENPPTO_BASE.MODIF_REG_CONT) + (SUM(V_RESUMENPPTO_BASE.REG_NO_CONTRACT + V_RESUMENPPTO_BASE.MODIF_REG_NOCONT))) DEFICITOEXCEDENTEPAC,
  ((SUM(V_RESUMENPPTO_BASE.REG_CONTRACT + V_RESUMENPPTO_BASE.MODIF_REG_CONT)) + (SUM(V_RESUMENPPTO_BASE.REG_NO_CONTRACT + V_RESUMENPPTO_BASE.MODIF_REG_NOCONT))) - SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT) SALDOCOMPROMISOSPORPAGAR
FROM V_RESUMENPPTO_BASE
INNER JOIN V_SALDO_PLAN_PPTAL
ON V_RESUMENPPTO_BASE.COMPANIA    = V_SALDO_PLAN_PPTAL.COMPANIA
AND V_RESUMENPPTO_BASE.ANO        = V_SALDO_PLAN_PPTAL.ANO
AND V_RESUMENPPTO_BASE.CODIGO     = V_SALDO_PLAN_PPTAL.CODIGO
AND V_RESUMENPPTO_BASE.MES        =V_SALDO_PLAN_PPTAL.MES
WHERE V_RESUMENPPTO_BASE.COMPANIA = 's$compania$s'
AND V_RESUMENPPTO_BASE.ANO        = s$ano$s
AND V_RESUMENPPTO_BASE.MES BETWEEN 0 AND s$mes$s
AND V_RESUMENPPTO_BASE.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
AND V_RESUMENPPTO_BASE.NATURALEZA IN('D')
GROUP BY V_RESUMENPPTO_BASE.COMPANIA,
  V_RESUMENPPTO_BASE.ANO,
  V_RESUMENPPTO_BASE.CODIGO,
  V_RESUMENPPTO_BASE.NOMBRE
