WITH INGRESOS_FL1 AS (
SELECT 
  SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,
  SALDO_PLAN_PPTAL.CODIGO CUENTA,
  SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' THEN APROPIACION_DEBITO-APROPIACION_CREDITO ELSE APROPIACION_CREDITO-APROPIACION_DEBITO END) AS APROPIADO,
  SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'THEN APLAZAM_DEBITO    -APLAZAM_CREDITO ELSE APLAZAM_CREDITO-APLAZAM_DEBITO END)             AS APLAZAMIENTO,
  SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' THEN TRASLADO_DEBITO   -TRASLADO_CREDITO ELSE TRASLADO_CREDITO-TRASLADO_DEBITO END)          AS TRASLADO,
  SUM(SALDO_PLAN_PPTAL.ADICION)                                                                                            AS ADICION,
  SUM(SALDO_PLAN_PPTAL.REDUCCION)                                                                                          AS REDUCCION,
  PLAN_PRESUPUESTAL.NOMBRE,
  SUM((CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' THEN EJE_PPT_DEBITO-EJE_PPT_CREDITO ELSE EJE_PPT_CREDITO-EJE_PPT_DEBITO END)+MODIF_INGRESOS_EFECTIVO+MODIF_INGRESOS_PAPELES) AS RECAUDOSACUMULADOS
FROM   PLAN_PRESUPUESTAL
INNER JOIN  SALDO_AUX_PPTAL SALDO_PLAN_PPTAL ON
  PLAN_PRESUPUESTAL.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA AND
  PLAN_PRESUPUESTAL.ANO      = SALDO_PLAN_PPTAL.ANO AND
  PLAN_PRESUPUESTAL.CODIGO   = SALDO_PLAN_PPTAL.CODIGO
WHERE SALDO_PLAN_PPTAL.COMPANIA=s$compania$s
AND SALDO_PLAN_PPTAL.ANO         = s$anio$s
AND SALDO_PLAN_PPTAL.MES <= s$mes$s
AND SALDO_PLAN_PPTAL.AUXILIAR 
  BETWEEN 's$fuenteInicial$s' AND 's$fuenteFinal$s'
AND PLAN_PRESUPUESTAL.CODIGO 
   BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
AND PLAN_PRESUPUESTAL.NATURALEZA='C'
AND PLAN_PRESUPUESTAL.MAN_AUX_GEN NOT IN (0)
GROUP BY SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,
    PLAN_PRESUPUESTAL.NATURALEZA,
    SALDO_PLAN_PPTAL.CODIGO,
  PLAN_PRESUPUESTAL.CODIGO,
  PLAN_PRESUPUESTAL.NOMBRE
  ),

 TMPLisEjecPptalIngresos_FL AS(
SELECT 
PLAN_PRESUPUESTAL.COMPANIA,
PLAN_PRESUPUESTAL.CODIGO CUENTA,
 PLAN_PRESUPUESTAL.NOMBRE,
  SUM(INGRESOS_FL1.APROPIADO) APROPIADO,
  SUM(INGRESOS_FL1.ADICION+INGRESOS_FL1.TRASLADO+INGRESOS_FL1.REDUCCION+INGRESOS_FL1.APLAZAMIENTO)                                  AS MODIFICACIONES,
  SUM(INGRESOS_FL1.APROPIADO+INGRESOS_FL1.ADICION+INGRESOS_FL1.REDUCCION+INGRESOS_FL1.TRASLADO+INGRESOS_FL1.APLAZAMIENTO)           AS TOTALPRESUPUESTO,
  SUM(INGRESOS_FL1.RECAUDOSACUMULADOS-(EJE_PPT_CREDITO-EJE_PPT_DEBITO+MODIF_INGRESOS_EFECTIVO+MODIF_INGRESOS_PAPELES))              AS RECAUDOSANTERIORES,
  SUM(EJE_PPT_CREDITO-EJE_PPT_DEBITO+MODIF_INGRESOS_EFECTIVO+MODIF_INGRESOS_PAPELES)												AS RECAUDOSMES,
  SUM(INGRESOS_FL1.RECAUDOSACUMULADOS) RECAUDOSACUMULADOS,
  SUM((INGRESOS_FL1.APROPIADO+INGRESOS_FL1.ADICION+INGRESOS_FL1.REDUCCION+INGRESOS_FL1.TRASLADO+APLAZAMIENTO)-INGRESOS_FL1.RECAUDOSACUMULADOS) AS PORRECAUDAR,
(SUM(INGRESOS_FL1.RECAUDOSACUMULADOS)/ 
 CASE WHEN SUM(INGRESOS_FL1.APROPIADO+INGRESOS_FL1.ADICION+INGRESOS_FL1.REDUCCION+INGRESOS_FL1.TRASLADO+INGRESOS_FL1.APLAZAMIENTO) IN(0)
       THEN 1  
       ELSE SUM(INGRESOS_FL1.APROPIADO+INGRESOS_FL1.ADICION+INGRESOS_FL1.REDUCCION+INGRESOS_FL1.TRASLADO+INGRESOS_FL1.APLAZAMIENTO)
  END)*100  PORCRECAUDADO,
  0 MES
FROM (INGRESOS_FL1
INNER JOIN SALDO_PLAN_PPTAL ON
INGRESOS_FL1.COMPANIA  = SALDO_PLAN_PPTAL.COMPANIA AND
INGRESOS_FL1.ANO       = SALDO_PLAN_PPTAL.ANO AND
INGRESOS_FL1.CUENTA    = SALDO_PLAN_PPTAL.CODIGO
)
INNER JOIN  PLAN_PRESUPUESTAL ON
INGRESOS_FL1.COMPANIA  = PLAN_PRESUPUESTAL.COMPANIA AND
INGRESOS_FL1.ANO       = PLAN_PRESUPUESTAL.ANO AND
LENGTH(PLAN_PRESUPUESTAL.CODIGO) <= LENGTH(INGRESOS_FL1.CUENTA)
AND (PLAN_PRESUPUESTAL.CODIGO)      = SUBSTR(INGRESOS_FL1.CUENTA, 1, LENGTH(PLAN_PRESUPUESTAL.CODIGO))
WHERE (APROPIADO + ABS(INGRESOS_FL1.ADICION+INGRESOS_FL1.TRASLADO+INGRESOS_FL1.REDUCCION+INGRESOS_FL1.APLAZAMIENTO) + (INGRESOS_FL1.APROPIADO+INGRESOS_FL1.ADICION+INGRESOS_FL1.REDUCCION+INGRESOS_FL1.TRASLADO+INGRESOS_FL1.APLAZAMIENTO) + RECAUDOSACUMULADOS + ABS((INGRESOS_FL1.APROPIADO+INGRESOS_FL1.ADICION+INGRESOS_FL1.REDUCCION+INGRESOS_FL1.TRASLADO+INGRESOS_FL1.APLAZAMIENTO)-RECAUDOSACUMULADOS) + (EJE_PPT_CREDITO - EJE_PPT_DEBITO + MODIF_INGRESOS_EFECTIVO + MODIF_INGRESOS_PAPELES)) <> 0
  AND SALDO_PLAN_PPTAL.MES = s$mes$s
  AND PLAN_PRESUPUESTAL.MAN_AUX_GEN NOT IN (0)
GROUP BY PLAN_PRESUPUESTAL.COMPANIA,
PLAN_PRESUPUESTAL.CODIGO,
PLAN_PRESUPUESTAL.NOMBRE  
ORDER BY
  PLAN_PRESUPUESTAL.CODIGO)
  
   SELECT 
  TMPLisEjecPptalIngresos_FL.COMPANIA,
  ' ' AS CUENTA,
  ' ' AS NOMBRE,
  SUM(TMPLisEjecPptalIngresos_FL.APROPIADO) APROPIADO,
  SUM(TMPLisEjecPptalIngresos_FL.MODIFICACIONES) MODIFICACIONES,
  SUM(TMPLisEjecPptalIngresos_FL.TOTALPRESUPUESTO) TOTALPRESUPUESTO,
  SUM(TMPLisEjecPptalIngresos_FL.RECAUDOSANTERIORES) RECAUDOSANTERIORES,
  SUM(TMPLisEjecPptalIngresos_FL.RECAUDOSMES) RECAUDOSMES,
  SUM(TMPLisEjecPptalIngresos_FL.RECAUDOSACUMULADOS) RECAUDOSACUMULADOS,
  SUM(TMPLisEjecPptalIngresos_FL.PORRECAUDAR) PORRECAUDAR,
  SUM(TMPLisEjecPptalIngresos_FL.PORCRECAUDADO) PORCRECAUDADO,
  0 MES
  FROM TMPLisEjecPptalIngresos_FL
  WHERE LENGTH(TMPLisEjecPptalIngresos_FL.CUENTA) = 1 
  GROUP BY 
  COMPANIA,CUENTA,NOMBRE
  UNION ALL
  SELECT * FROM TMPLisEjecPptalIngresos_FL
