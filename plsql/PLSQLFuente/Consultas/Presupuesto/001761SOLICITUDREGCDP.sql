WITH ORDENADORGASTO AS (
  SELECT ORDENADOR.COMPANIA, ORDENADOR.CEDULA, TERCERO.NOMBRE NOMBRE 
FROM ORDENADOR
INNER JOIN TERCERO 
ON  ORDENADOR.COMPANIA = TERCERO.COMPANIA
AND ORDENADOR.CEDULA   = TERCERO.NIT
AND ORDENADOR.SUCURSAL = TERCERO.SUCURSAL
WHERE IND_ACTIVO NOT IN (-1)
AND ROWNUM = 1)
SELECT TO_CHAR(SYSDATE, 'DD') DIAACT,
  TO_CHAR(SYSDATE, 'MM') MESACT,
  TO_CHAR(SYSDATE, 'YYYY') ANOACT,
  TO_CHAR(SOLICITUDDISPONIBILIDAD.FECHA, 'DD') DIASOL,
  TO_CHAR(SOLICITUDDISPONIBILIDAD.FECHA, 'MM') MESSOL,
  TO_CHAR(SOLICITUDDISPONIBILIDAD.FECHA, 'YYYY') ANOSOL,
  TO_CHAR(SOLICITUDDISPONIBILIDAD.FECHA_APROBACION, 'DD') DIAAPROB,
  TO_CHAR(SOLICITUDDISPONIBILIDAD.FECHA_APROBACION, 'MM') MESAPROB,
  TO_CHAR(SOLICITUDDISPONIBILIDAD.FECHA_APROBACION, 'YYYY') ANOAPROB,
  SOLICITUDDISPONIBILIDAD.NUMERO,
  TERCERO.NOMBRE NOMBRESOLICITANTE,
  ORDENADORGASTO.NOMBRE NOMBREORDENGASTO,
  DEPENDENCIA.NOMBRE NOMBREDEPENDENCIA,
  RESPONSABLE.CARGO CARGOSOLICITANTE,
  D_SOLICITUDDISPONIBILIDAD.RUBRO,
  PLAN_PRESUPUESTAL.NOMBRE DENOMINACION,
  BP_PLAN_ADQUISICIONES.DESCRIPCION NOMBREBIEN,
  D_SOLICITUDDISPONIBILIDAD.VALOR VALORBIEN,
  CASE
    WHEN PLAN_PRESUPUESTAL.DESTINO = 'I'
    THEN 'X'
    ELSE ' '
  END INVERSION,
  CASE
    WHEN PLAN_PRESUPUESTAL.DESTINO = 'F'
    THEN 'X'
    ELSE ' '
  END FUNCIONAMIENTO,
  SOLICITUDDISPONIBILIDAD.APROBACION,
  TIPOS.ID,
  SOLICITUDDISPONIBILIDAD.NRO_SOLICITUD_AFECT,
  CASE
    WHEN BP_PLAN_ADQUISICIONES.PAA NOT IN (0)
    THEN 'X'
    ELSE ' '
  END SIPAA,
  CASE
    WHEN BP_PLAN_ADQUISICIONES.PAA IN (0)
    THEN 'X'
    ELSE ' '
  END NOPAA,
  (SELECT SOLICITUDDISPONIBILIDAD.NUMERO
  FROM SOLICITUDDISPONIBILIDAD
  WHERE SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD    = 2
  AND SOLICITUDDISPONIBILIDAD.NRO_SOLICITUD_AFECT = s$numeroSolicitud$s
  ) CPD_Q_AFECT_INC,
  (SELECT SOLICITUDDISPONIBILIDAD.NUMERO
  FROM SOLICITUDDISPONIBILIDAD
  WHERE SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD    = 3
  AND SOLICITUDDISPONIBILIDAD.NRO_SOLICITUD_AFECT = s$numeroSolicitud$s
  )CPD_Q_AFECT_DEC ,
  NVL(
  (SELECT TO_CHAR(D_SOLICITUDDISPONIBILIDAD.VALOR)
  FROM SOLICITUDDISPONIBILIDAD
  INNER JOIN D_SOLICITUDDISPONIBILIDAD
  ON D_SOLICITUDDISPONIBILIDAD.COMPANIA           = SOLICITUDDISPONIBILIDAD.COMPANIA
  AND D_SOLICITUDDISPONIBILIDAD.NUMERO            = SOLICITUDDISPONIBILIDAD.NUMERO
  AND D_SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD    = SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD
  WHERE SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD    = 2
  AND SOLICITUDDISPONIBILIDAD.NRO_SOLICITUD_AFECT = s$numeroSolicitud$s
  ), ' ') VLR_OPE_INC,
  NVL(
  (SELECT TO_CHAR(D_SOLICITUDDISPONIBILIDAD.VALOR)
  FROM SOLICITUDDISPONIBILIDAD
  INNER JOIN D_SOLICITUDDISPONIBILIDAD
  ON D_SOLICITUDDISPONIBILIDAD.COMPANIA           = SOLICITUDDISPONIBILIDAD.COMPANIA
  AND D_SOLICITUDDISPONIBILIDAD.NUMERO            = SOLICITUDDISPONIBILIDAD.NUMERO
  AND D_SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD    = SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD
  WHERE SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD    = 3
  AND SOLICITUDDISPONIBILIDAD.NRO_SOLICITUD_AFECT = s$numeroSolicitud$s
  ), ' ') VLR_OPE_DEC,
  CASE
    WHEN (SELECT SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD
      FROM SOLICITUDDISPONIBILIDAD
      WHERE SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD    = 2
      AND SOLICITUDDISPONIBILIDAD.NRO_SOLICITUD_AFECT = s$numeroSolicitud$s ) IS NULL
    THEN ' '
    ELSE 'X'
  END INC_CDP,
  CASE
    WHEN (SELECT SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD
      FROM SOLICITUDDISPONIBILIDAD
      WHERE SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD    = 3
      AND SOLICITUDDISPONIBILIDAD.NRO_SOLICITUD_AFECT = s$numeroSolicitud$s ) IS NULL
    THEN ' '
    ELSE 'X'
  END DEC_CDP,
  CASE
    WHEN SOLICITUDDISPONIBILIDAD.APROBACION IN (0)
    THEN 'X'
    ELSE ' '
  END RECHAZADA,
  CASE
    WHEN SOLICITUDDISPONIBILIDAD.APROBACION IN (-1)
    THEN 'X'
    ELSE ' '
  END APROBADA,
  NVL(SOLICITUDDISPONIBILIDAD.OBJETO,' ') OBJETOSOLICITUD,
  NVL(BP_PLAN_INDICATIVO.DESCRIPCION,' ') DESCRIPCIONINDICATIVO
FROM SOLICITUDDISPONIBILIDAD
INNER JOIN TERCERO
ON SOLICITUDDISPONIBILIDAD.COMPANIA  = TERCERO.COMPANIA
AND SOLICITUDDISPONIBILIDAD.TERCERO  = TERCERO.NIT
AND SOLICITUDDISPONIBILIDAD.SUCURSAL = TERCERO.SUCURSAL
LEFT JOIN RESPONSABLE
ON TERCERO.COMPANIA  = RESPONSABLE.COMPANIA
AND TERCERO.NIT      = RESPONSABLE.CEDULA
AND TERCERO.SUCURSAL = RESPONSABLE.SUCURSAL
INNER JOIN DEPENDENCIA
ON SOLICITUDDISPONIBILIDAD.COMPANIA     = DEPENDENCIA.COMPANIA
AND SOLICITUDDISPONIBILIDAD.DEPENDENCIA = DEPENDENCIA.CODIGO
LEFT JOIN DEPENDENCIA_RESPONSABLE
ON DEPENDENCIA_RESPONSABLE.COMPANIA     = RESPONSABLE.COMPANIA
AND DEPENDENCIA_RESPONSABLE.RESPONSABLE = RESPONSABLE.CEDULA
AND DEPENDENCIA_RESPONSABLE.SUCURSAL    = RESPONSABLE.SUCURSAL
AND DEPENDENCIA_RESPONSABLE.COMPANIA    = DEPENDENCIA.COMPANIA
AND DEPENDENCIA_RESPONSABLE.DEPENDENCIA = DEPENDENCIA.CODIGO
INNER JOIN D_SOLICITUDDISPONIBILIDAD
ON D_SOLICITUDDISPONIBILIDAD.COMPANIA        = SOLICITUDDISPONIBILIDAD.COMPANIA
AND D_SOLICITUDDISPONIBILIDAD.NUMERO         = SOLICITUDDISPONIBILIDAD.NUMERO
AND D_SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD = SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD
INNER JOIN PLAN_PRESUPUESTAL
ON D_SOLICITUDDISPONIBILIDAD.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
AND D_SOLICITUDDISPONIBILIDAD.ANO     = PLAN_PRESUPUESTAL.ANO
AND D_SOLICITUDDISPONIBILIDAD.RUBRO   = PLAN_PRESUPUESTAL.CODIGO
LEFT JOIN BP_PLAN_ADQUISICIONES
ON D_SOLICITUDDISPONIBILIDAD.COMPANIA          = BP_PLAN_ADQUISICIONES.COMPANIA
AND D_SOLICITUDDISPONIBILIDAD.VIGENCIA_INICIAL = BP_PLAN_ADQUISICIONES.VIGENCIA_INICIAL
AND D_SOLICITUDDISPONIBILIDAD.ID_PLAN          = BP_PLAN_ADQUISICIONES.ID_PLAN
AND D_SOLICITUDDISPONIBILIDAD.CODIGO           = BP_PLAN_ADQUISICIONES.CODIGO
LEFT JOIN BP_PLAN_ADQUISICIONES_VIGENCIA
ON BP_PLAN_ADQUISICIONES.COMPANIA          = BP_PLAN_ADQUISICIONES_VIGENCIA.COMPANIA
AND BP_PLAN_ADQUISICIONES.VIGENCIA_INICIAL = BP_PLAN_ADQUISICIONES_VIGENCIA.VIGENCIA_INICIAL
AND BP_PLAN_ADQUISICIONES.ID_PLAN          = BP_PLAN_ADQUISICIONES_VIGENCIA.ID_PLAN
AND BP_PLAN_ADQUISICIONES.CODIGO           = BP_PLAN_ADQUISICIONES_VIGENCIA.CODIGO
INNER JOIN TIPOS
ON SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD = TIPOS.ID
LEFT JOIN SOLICITUDDISPONIBILIDAD SDAFECT
ON SDAFECT.NRO_SOLICITUD_AFECT       = SOLICITUDDISPONIBILIDAD.NUMERO
LEFT JOIN BP_PLAN_INDICATIVO
ON  D_SOLICITUDDISPONIBILIDAD.COMPANIA            = BP_PLAN_INDICATIVO.COMPANIA
AND D_SOLICITUDDISPONIBILIDAD.ID_PLAN_INDICATIVO  = BP_PLAN_INDICATIVO.ID
AND D_SOLICITUDDISPONIBILIDAD.VIGENCIA_INICIAL    = BP_PLAN_INDICATIVO.VIGENCIA_INICIAL,
ORDENADORGASTO
WHERE SOLICITUDDISPONIBILIDAD.NUMERO = s$numeroSolicitud$s
AND SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD = s$tipoSolicitud$s
