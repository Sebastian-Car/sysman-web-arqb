MERGE INTO CONSULTAS FIN USING (SELECT '002351DISCASTILLA' INFORME ,TO_CLOB(q'[SELECT VISTA.COMPROBANTE COMPROBANTE,
  TO_CHAR(COMPROBANTE_PPTAL.FECHA,'DD/MM/YYYY')FECHA,
  COMPROBANTE_PPTAL.TERCERO TERCERONIT,
  TO_CHAR(VISTA.FECHA_COM,'DD/MM/YYYY')FECHA_COM,
  TERCERO.NOMBRE NOMBRE_TERCERO,
  COMPROBANTE_PPTAL.SUCURSAL,
  VISTA.CUENTANOMBRE NOMBRE,
  VISTA.VALOR_DEBITO,
  COMPROBANTE_PPTAL.CARGO,
  VISTA.TEXTO TXT,
  VISTA.DESCRIPCION|| VISTA.TEXTO DESCRIPCION,
  TO_CHAR(VISTA.FECHA_VCN_DOC,'DD/MM/YYYY')FECHA_VCN_DOC,
  DEPENDENCIA.NOMBRE NOMBREPENDENCIA,
  VISTA.CUENTA CODIGO_CUENTA,
  (CASE
    WHEN VISTA.NATURALEZA   IN ('D')
    THEN (VISTA.VALOR_DEBITO -VALOR_CREDITO) - (VISTA.DEBITO_AFECTADO - VISTA.CREDITO_AFECTADO)+ (VISTA.MODIFICACION_DEBITO - VISTA.MODIFICACION_CREDITO)
    ELSE VISTA.VALOR_CREDITO + VISTA.MODIFICACION_CREDITO - VISTA.MODIFICACION_DEBITO - VISTA.CREDITO_AFECTADO
  END) SALDODIS,
  VISTA.CREATED_BY ELABORO 
FROM COMPROBANTE_PPTAL
INNER JOIN V_DETALLE_AUXILIAR_PPTAL VISTA
ON  COMPROBANTE_PPTAL.COMPANIA = VISTA.COMPANIA
AND COMPROBANTE_PPTAL.ANO = VISTA.ANO
AND COMPROBANTE_PPTAL.TIPO = VISTA.TIPO
AND COMPROBANTE_PPTAL.NUMERO = VISTA.COMPROBANTE
INNER JOIN TERCERO
ON  COMPROBANTE_PPTAL.COMPANIA = TERCERO.COMPANIA
AND COMPROBANTE_PPTAL.TERCERO  = TERCERO.NIT
AND COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL
LEFT JOIN DEPENDENCIA
  ON VISTA.COMPANIA     = DEPENDENCIA.COMPANIA
  AND VISTA.DEPENDENCIA = DEPENDENCIA.CODIGO
WHERE VISTA.COMPANIA       = s$compania$s
  AND VISTA.ANO            = s$ano$s
  AND VISTA.TIPO           = 's$tipoCpte$s'
  AND VISTA.COMPROBANTE BETWEEN s$numeroPptoInicial$s AND s$numeroPptoFinal$s 
ORDER BY VISTA.CUENTA]') CONSULTA, 3 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);