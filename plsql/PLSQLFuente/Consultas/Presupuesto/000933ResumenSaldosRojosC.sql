SELECT DETALLE_COMPROBANTE_PPTAL.COMPANIA, 
       DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE, 
       DETALLE_COMPROBANTE_PPTAL.COMPROBANTE, 
       DETALLE_COMPROBANTE_PPTAL.CUENTA, 
       PLAN_PRESUPUESTAL.NOMBRE PLAN_PRESUPUESTAL_NOMBRE,  
       TO_CHAR(DETALLE_COMPROBANTE_PPTAL.FECHA, 'DD/MM/YYYY')  FECHA, 
       (CASE WHEN DETALLE_COMPROBANTE_PPTAL.NATURALEZA='D' THEN
                  (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)-(DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO)+(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO)
             ELSE (DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO-DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO)-(DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO)+(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO) 
        END) VALOR, 
       DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO, 
       DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO, 
       DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO, 
       DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO,
       DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO , 
       DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO, 
       TIPO_COMPROBPP.CLASE, 
       TIPO_COMPROBPP.NOMBRE TIPO_COMPROBPP_NOMBRE
FROM DETALLE_COMPROBANTE_PPTAL 
      LEFT JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
           ON DETALLE_COMPROBANTE_PPTAL.COMPANIA  = PLAN_PRESUPUESTAL.COMPANIA 
          AND DETALLE_COMPROBANTE_PPTAL.ANO       = PLAN_PRESUPUESTAL.ANO 
          AND DETALLE_COMPROBANTE_PPTAL.ID   = PLAN_PRESUPUESTAL.ID 
      LEFT JOIN TIPO_COMPROBPP 
           ON DETALLE_COMPROBANTE_PPTAL.COMPANIA  = TIPO_COMPROBPP.COMPANIA 
          AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO 
      LEFT JOIN CLASECNTPRES 
           ON TIPO_COMPROBPP.CLASE = CLASECNTPRES.CODIGO
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA = s$compania$s 
  AND DETALLE_COMPROBANTE_PPTAL.FECHA BETWEEN TO_DATE('s$fechaInicial$s', 'DD/MM/YYYY')  AND TO_DATE('s$fechaFinal$s', 'DD/MM/YYYY')  
  AND TIPO_COMPROBPP.CLASE 		 IN ('DIS', 'EGR', 'EJE', 'ICA', 'ING', 'REA', 'REO', 'RES')
  AND (CASE WHEN DETALLE_COMPROBANTE_PPTAL.NATURALEZA = 'D' THEN
                 (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)-(DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO)+(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO)
            ELSE (DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO-DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO)-(DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO)+(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO)
      END) < 0
