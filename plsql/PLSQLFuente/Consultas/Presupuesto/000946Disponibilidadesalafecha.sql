SELECT  DETALLE_COMPROBANTE_PPTAL.ID,
        V_PLAN_PRESUPUESTAL.NOMBRE,
        V_PLAN_PRESUPUESTAL.APROPIACIONINICIAL +
       SUM(
          CASE WHEN TIPO_COMPROBPP.CLASE IN ('ADC','APL','RED','TRA') 
              THEN 
                CASE WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D' 
                  THEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO 
                  ELSE (DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO - DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO) 
                END 
              ELSE 0
           END  - 
           CASE WHEN TIPO_COMPROBPP.CLASE IN ('DIS','ADD','DMD')
              THEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO
              ELSE 0
           END) AS PODISPONER
FROM DETALLE_COMPROBANTE_PPTAL
          INNER JOIN TIPO_COMPROBPP
            ON DETALLE_COMPROBANTE_PPTAL.COMPANIA   = TIPO_COMPROBPP.COMPANIA 
            AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
          INNER JOIN V_PLAN_PRESUPUESTAL
            ON DETALLE_COMPROBANTE_PPTAL.COMPANIA  = V_PLAN_PRESUPUESTAL.COMPANIA 
            AND DETALLE_COMPROBANTE_PPTAL.ANO      = V_PLAN_PRESUPUESTAL.ANO
            AND DETALLE_COMPROBANTE_PPTAL.ID   = V_PLAN_PRESUPUESTAL.ID
WHERE  DETALLE_COMPROBANTE_PPTAL.COMPANIA = s$compania$s 
  AND TIPO_COMPROBPP.CLASE  IN ('ADC','APL','RED','TRA','DIS','ADD','DMD')
  AND DETALLE_COMPROBANTE_PPTAL.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
  AND V_PLAN_PRESUPUESTAL.ID BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
  AND V_PLAN_PRESUPUESTAL.NATURALEZA = 'D'  
GROUP BY DETALLE_COMPROBANTE_PPTAL.ID,
         V_PLAN_PRESUPUESTAL.NOMBRE,
         V_PLAN_PRESUPUESTAL.APROPIACIONINICIAL
