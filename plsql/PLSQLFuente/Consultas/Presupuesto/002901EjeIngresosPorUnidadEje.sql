FROM TIPOCLASIFICADOR P INNER JOIN (SELECT  ANO,COMPANIA,
  CODIGO,
  NOMBRE,
  CCP,
  CCPNOMBRE,
  FUENTE,
  FUENTENOMBRE,
  FONDOS,
  SUM(APROPINICIAL) APINICIAL,
  SUM(PPTO_DEFINITIVO) P_DEFINITIVO,
  SUM(NVL(ENE,0)) + SUM(NVL(FEB,0))  + SUM(NVL(MAR,0)) +  SUM(NVL(ABR,0)) +  SUM(NVL(MAY,0))+  SUM(NVL(JUN,0))+
  SUM(NVL(JUL,0)) +  SUM(NVL(AGO,0))+  SUM(NVL(SEP,0))+  SUM(NVL(OCT,0)) +   SUM(NVL(NOV,0)) +  SUM(NVL(DIC,0)) GItotal,
