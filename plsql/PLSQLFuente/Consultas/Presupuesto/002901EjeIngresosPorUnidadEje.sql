SUM(F.APINICIAL) APRO_INICIAL,
SUM(F.P_DEFINITIVO) PPTO_DEFINITIVO,SUM(F.GItotal) GItotal,
SUM(F.P_DEFINITIVO-GItotal) GISALDO,
SUM(ENE) ENEGIRO,
SUM(FEB) FEBGIRO,
SUM(MAR) MARGIRO,
SUM(ABR) ABRILGIRO,
SUM(MAY) MAYGIRO,
SUM(JUN) JUNGIRO,
SUM(JUL) JULGIRO,
SUM(AGO) AGOGIRO,
SUM(SEP) SEPGIRO,
SUM(OCT) OCTGIRO,
SUM(NOV) NOVGIRO,
