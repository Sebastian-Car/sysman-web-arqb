  SUM(NVL(FEB,0)) FEB ,
  SUM(NVL(MAR,0)) MAR ,
  SUM(NVL(ABR,0)) ABR,
  SUM(NVL(MAY,0)) MAY,
  SUM(NVL(JUN,0)) JUN,
  SUM(NVL(JUL,0)) JUL,
  SUM(NVL(AGO,0)) AGO,
  SUM(NVL(SEP,0))SEP,
  SUM(NVL(OCT,0)) OCT,
  SUM(NVL(NOV,0)) NOV,
  SUM(NVL(DIC,0)) DIC

FROM(SELECT  SALDO.MES,INI.COMPANIA,INI.ANO,NVL(INI.CODIGOCCPET_MAN,INI.CODIGOCCPET_PAD) CODIGO ,       
    (SELECT NOMBRE FROM TIPOCLASIFICADOR WHERE CODIGO = NVL(INI.CODIGOCCPET_MAN, INI.CODIGOCCPET_PAD) AND ANO =s$ano$s  AND COMPANIA = s$compania$s AND CLASECLASIFICADOR = '006' AND ROWNUM =1) NOMBRE,             
    NVL(NVL(INI.CODIGOCCP_MAN,INI.CODIGOCCP_PAD),0) CCP,
    NVL(TCDANE.NOMBRE, '-') CCPNOMBRE,
    NVL(NVL(INI.CODIGOFUENTE_MAN,INI.CODIGOFUENTE_PAD),0) FUENTE,
    NVL(TCF.NOMBRE, '-') FUENTENOMBRE,
    CASE WHEN INI.CONSITUACIONFONDOS = -1 THEN 'S' ELSE 'C' END FONDOS,
     SUM(EJE_PPT_CREDITO - EJE_PPT_DEBITO) VALOR,
     SUM(APROPIACION_CREDITO-APROPIACION_DEBITO) APROPINICIAL,
    SUM(    - APROPIACION_DEBITO  + APROPIACION_CREDITO + TRASLADO_CREDITO - TRASLADO_DEBITO - Saldo.APLAZAM_DEBITO + Saldo.APLAZAM_CREDITO + ADICION + REDUCCION) AS PPTO_DEFINITIVO,
    1 GITOTAL
    FROM (SELECT MOV.COMPANIA,
    MOV.ANO,                
    MOV.CODIGO,               
    MAX(CASE WHEN PADRE.CLASECLASIFICADOR ='006' THEN
    PADRE.TIPOCLASIFICADOR 
    ELSE 
    NULL END) CODIGOCCPET_PAD,
    MOV.CODIGOCCPET CODIGOCCPET_MAN,
    MAX(CASE WHEN PADRE.CLASECLASIFICADOR ='007' THEN
    PADRE.TIPOCLASIFICADOR 
    ELSE 
    NULL END) CODIGOCCP_PAD,
    MOV.CODIGOCPCDANE CODIGOCCP_MAN,
    MAX(CASE WHEN PADRE.CLASECLASIFICADOR ='009' THEN
    PADRE.TIPOCLASIFICADOR 
    ELSE 
    NULL END) CODIGOFUENTE_PAD,
    MOV.CODIGOFUENTE CODIGOFUENTE_MAN,
    MOV.CONSITUACIONFONDOS
    FROM PLAN_PRESUPUESTAL MOV 
]') || TO_CLOB(q'[    LEFT JOIN PLAN_PRESUPUESTAL PADRE 
    ON PADRE.COMPANIA = MOV.COMPANIA 
    AND PADRE.ANO = MOV.ANO 
    AND PADRE.CODIGO = SUBSTR(MOV.CODIGO,1,LENGTH(PADRE.CODIGO))
    WHERE MOV.COMPANIA = s$compania$s
    AND MOV.ANO      = s$ano$s 
    AND MOV.NATURALEZA = 'C'
    AND PADRE.CODIGO BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
    s$unidadeje$s
    AND MOV.REGALIAS IN(0) 
    AND (MOV.MOVIMIENTO<>0             
    OR MOV.MAN_AUX_FUE<>0            
    OR MOV.MAN_AUX_GEN<>0                       
    ) 
    
    GROUP BY MOV.COMPANIA,
    MOV.ANO,
    MOV.CODIGO,
    MOV.CODIGOCCPET,MOV.CODIGOCPCDANE,MOV.CODIGOFUENTE,MOV.CONSITUACIONFONDOS
    ORDER BY MOV.CODIGO
       ) INI
    INNER JOIN SALDO_AUX_PPTAL SALDO
    ON INI.COMPANIA = SALDO.COMPANIA
    AND INI.ANO      = SALDO.ANO  
    AND INI.CODIGO   = SALDO.CODIGO 
    LEFT JOIN (select codigo,nombre,compania,ano
    from tipoclasificador
    where  claseclasificador = 007) TCDANE
        ON INI.COMPANIA = TCDANE.COMPANIA
        AND INI.ANO = TCDANE.ANO AND NVL(INI.CODIGOCCP_MAN,INI.CODIGOCCP_PAD) = TCDANE.CODIGO  
    LEFT JOIN (select codigo,nombre,compania,ano
    from tipoclasificador
    where  claseclasificador = 009) TCF ON INI.COMPANIA = TCF.COMPANIA
            AND INI.ANO = TCF.ANO AND NVL(INI.CODIGOFUENTE_MAN,INI.CODIGOFUENTE_PAD) = TCF.CODIGO         
    WHERE SALDO.COMPANIA = s$compania$s       
    AND SALDO.ANO   = s$ano$s       
    AND SALDO.MES  <= s$mes$s
    AND NVL(INI.CODIGOCCPET_MAN, INI.CODIGOCCPET_PAD) IS NOT NULL
    GROUP BY SALDO.MES,
    NVL(INI.CODIGOCCPET_MAN,INI.CODIGOCCPET_PAD), 
    NVL(NVL(INI.CODIGOCCP_MAN,INI.CODIGOCCP_PAD),0),
    NVL(TCDANE.NOMBRE, '-'),
    NVL(NVL(INI.CODIGOFUENTE_MAN,INI.CODIGOFUENTE_PAD),0),
    NVL(TCF.NOMBRE, '-'), INI.COMPANIA,INI.ANO,
    CASE WHEN INI.CONSITUACIONFONDOS = -1 THEN 'S' ELSE 'C' END )
    PIVOT (SUM(VALOR) FOR MES IN (0 INI, 1 ENE ,2 FEB ,3 MAR,4 ABR,5 MAY,6 JUN,7 JUL,8 AGO,9 SEP,10 OCT,11 NOV,12 DIC, 13 FIN) )
  GROUP BY ANO,COMPANIA,CODIGO,
  NOMBRE,
  CCP,
  CCPNOMBRE,
  FUENTE,
  FUENTENOMBRE,
  FONDOS) F  
ON  P.COMPANIA = F.COMPANIA 
AND P.ANO = F.ANO 
AND P.CODIGO = SUBSTR(F.CODIGO,1,LENGTH(P.CODIGO))
AND P.CLASECLASIFICADOR = 006
GROUP BY P.COMPANIA,P.CODIGO,P.NOMBRE,CASE WHEN P.MOVIMIENTO <> 0 THEN F.CCP END,CASE WHEN P.MOVIMIENTO <> 0 THEN F.CCPNOMBRE END,
CASE WHEN P.MOVIMIENTO <> 0 THEN F.FUENTE END ,CASE WHEN P.MOVIMIENTO <> 0 THEN F.FUENTENOMBRE END ,CASE WHEN P.MOVIMIENTO <> 0 THEN F.FONDOS END
HAVING SUM(F.APINICIAL)+SUM(F.P_DEFINITIVO)+SUM(F.GItotal)+SUM(F.P_DEFINITIVO-GItotal) <>0
ORDER BY P.CODIGO]') CONSULTA, 3 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, 'NCARDENAS' CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);