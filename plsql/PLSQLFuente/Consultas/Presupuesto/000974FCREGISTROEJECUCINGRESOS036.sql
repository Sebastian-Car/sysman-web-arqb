WITH FC_REGISTROEJECUCINGRESOS_036 AS 
 (
    SELECT SALDO_PLAN_PPTAL.COMPANIA,
           SALDO_PLAN_PPTAL.ANO,
           SALDO_PLAN_PPTAL.ID CUENTA, 
           PLAN_PRESUPUESTAL.NOMBRE, 
           SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA = 'D'
                    THEN SALDO_PLAN_PPTAL.APROPIACION_DEBITO - SALDO_PLAN_PPTAL.APROPIACION_CREDITO
                    ELSE SALDO_PLAN_PPTAL.APROPIACION_CREDITO - SALDO_PLAN_PPTAL.APROPIACION_DEBITO 
               END) APROPIADO,
          SUM(SALDO_PLAN_PPTAL.ADICION) ADICION,
          SUM(SALDO_PLAN_PPTAL.REDUCCION) REDUCCION,
          SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO) CREDITO,
          SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO) CONTRACREDITO,
          SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA = 'D'
                   THEN SALDO_PLAN_PPTAL.APLAZAM_DEBITO - SALDO_PLAN_PPTAL.APLAZAM_CREDITO
                   ELSE SALDO_PLAN_PPTAL.APLAZAM_CREDITO - SALDO_PLAN_PPTAL.APLAZAM_DEBITO
              END) APLAZAMIENTO, 
          SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA = 'D'
                    THEN SALDO_PLAN_PPTAL.TRASLADO_DEBITO - SALDO_PLAN_PPTAL.TRASLADO_CREDITO 
                    ELSE SALDO_PLAN_PPTAL.TRASLADO_CREDITO - SALDO_PLAN_PPTAL.TRASLADO_DEBITO 
               END) TRASLADO, 
          CASE WHEN s$enMiles$s NOT IN (0)
                THEN ROUND((SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA = 'D'
                                     THEN SALDO_PLAN_PPTAL.APROPIACION_DEBITO - SALDO_PLAN_PPTAL.APROPIACION_CREDITO
                                     ELSE SALDO_PLAN_PPTAL.APROPIACION_CREDITO - SALDO_PLAN_PPTAL.APROPIACION_DEBITO 
                                END)+ SUM(SALDO_PLAN_PPTAL.ADICION) +
                            SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA = 'D'
                                      THEN SALDO_PLAN_PPTAL.TRASLADO_DEBITO - SALDO_PLAN_PPTAL.TRASLADO_CREDITO 
                                      ELSE SALDO_PLAN_PPTAL.TRASLADO_CREDITO - SALDO_PLAN_PPTAL.TRASLADO_DEBITO 
                                END) + SUM(SALDO_PLAN_PPTAL.REDUCCION) +
                            SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA = 'D'
                                      THEN SALDO_PLAN_PPTAL.APLAZAM_DEBITO - SALDO_PLAN_PPTAL.APLAZAM_CREDITO
                                      ELSE SALDO_PLAN_PPTAL.APLAZAM_CREDITO - SALDO_PLAN_PPTAL.APLAZAM_DEBITO
                                END)),-3)/1000 
                ELSE (SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA = 'D'
                               THEN SALDO_PLAN_PPTAL.APROPIACION_DEBITO - SALDO_PLAN_PPTAL.APROPIACION_CREDITO
                               ELSE SALDO_PLAN_PPTAL.APROPIACION_CREDITO - SALDO_PLAN_PPTAL.APROPIACION_DEBITO 
                          END) + SUM(SALDO_PLAN_PPTAL.ADICION) +
                     SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA = 'D'
                              THEN SALDO_PLAN_PPTAL.TRASLADO_DEBITO - SALDO_PLAN_PPTAL.TRASLADO_CREDITO 
                              ELSE SALDO_PLAN_PPTAL.TRASLADO_CREDITO - SALDO_PLAN_PPTAL.TRASLADO_DEBITO 
                         END)) + SUM(SALDO_PLAN_PPTAL.REDUCCION) +
                     SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA = 'D'
                              THEN SALDO_PLAN_PPTAL.APLAZAM_DEBITO - SALDO_PLAN_PPTAL.APLAZAM_CREDITO
                              ELSE SALDO_PLAN_PPTAL.APLAZAM_CREDITO - SALDO_PLAN_PPTAL.APLAZAM_DEBITO
                         END)
          END APROPIACIONVIGENTE, 
          CASE WHEN s$enMiles$s NOT IN (0)
               THEN ROUND(SUM(SALDO_PLAN_PPTAL.INGRESOS_CAUSADOS + SALDO_PLAN_PPTAL.MODIF_INGRESOS_CAUSADOS),-3)/1000
               ELSE SUM(SALDO_PLAN_PPTAL.INGRESOS_CAUSADOS + SALDO_PLAN_PPTAL.MODIF_INGRESOS_CAUSADOS)
          END DERECHOSACUM, 
          CASE WHEN s$enMiles$s NOT IN (0)
               THEN ROUND(SUM(SALDO_PLAN_PPTAL.INGRESOS_EFECTIVO + SALDO_PLAN_PPTAL.MODIF_INGRESOS_EFECTIVO),-3)/1000
               ELSE SUM(SALDO_PLAN_PPTAL.INGRESOS_EFECTIVO + SALDO_PLAN_PPTAL.MODIF_INGRESOS_EFECTIVO)
          END EFECTIVOACUM, 
          CASE WHEN s$enMiles$s NOT IN (0)
               THEN ROUND(SUM(SALDO_PLAN_PPTAL.INGRESOS_PAPELES + SALDO_PLAN_PPTAL.MODIF_INGRESOS_PAPELES),-3)/1000 
               ELSE SUM(SALDO_PLAN_PPTAL.INGRESOS_PAPELES + SALDO_PLAN_PPTAL.MODIF_INGRESOS_PAPELES)
          END PAPELESACUM,
          CASE WHEN s$enMiles$s NOT IN (0) 
               THEN CASE WHEN s$enMiles$s NOT IN (0)
                         THEN ROUND(SUM(SALDO_PLAN_PPTAL.INGRESOS_CAUSADOS + SALDO_PLAN_PPTAL.MODIF_INGRESOS_CAUSADOS),-3)/1000
                         ELSE SUM(SALDO_PLAN_PPTAL.INGRESOS_CAUSADOS + SALDO_PLAN_PPTAL.MODIF_INGRESOS_CAUSADOS)
                    END -
                    CASE WHEN s$enMiles$s NOT IN (0)
                         THEN ROUND(SUM(SALDO_PLAN_PPTAL.INGRESOS_EFECTIVO + SALDO_PLAN_PPTAL.MODIF_INGRESOS_EFECTIVO),-3)/1000
                         ELSE SUM(SALDO_PLAN_PPTAL.INGRESOS_EFECTIVO + SALDO_PLAN_PPTAL.MODIF_INGRESOS_EFECTIVO)
                    END -
                    CASE WHEN s$enMiles$s NOT IN (0)
                         THEN ROUND(SUM(SALDO_PLAN_PPTAL.INGRESOS_PAPELES + SALDO_PLAN_PPTAL.MODIF_INGRESOS_PAPELES),-3)/1000 
                         ELSE SUM(SALDO_PLAN_PPTAL.INGRESOS_PAPELES + SALDO_PLAN_PPTAL.MODIF_INGRESOS_PAPELES)
                    END
          ELSE CASE WHEN s$enMiles$s NOT IN (0)
                    THEN ROUND(SUM(SALDO_PLAN_PPTAL.INGRESOS_CAUSADOS + SALDO_PLAN_PPTAL.MODIF_INGRESOS_CAUSADOS),-3)/1000
                    ELSE SUM(SALDO_PLAN_PPTAL.INGRESOS_CAUSADOS + SALDO_PLAN_PPTAL.MODIF_INGRESOS_CAUSADOS)
               END -
               CASE WHEN s$enMiles$s NOT IN (0)
                   THEN ROUND(SUM(SALDO_PLAN_PPTAL.INGRESOS_EFECTIVO + SALDO_PLAN_PPTAL.MODIF_INGRESOS_EFECTIVO),-3)/1000
                   ELSE SUM(SALDO_PLAN_PPTAL.INGRESOS_EFECTIVO + SALDO_PLAN_PPTAL.MODIF_INGRESOS_EFECTIVO)
               END - 
               CASE WHEN s$enMiles$s NOT IN (0)
                   THEN ROUND(SUM(SALDO_PLAN_PPTAL.INGRESOS_PAPELES + SALDO_PLAN_PPTAL.MODIF_INGRESOS_PAPELES),-3)/1000 
                   ELSE SUM(SALDO_PLAN_PPTAL.INGRESOS_PAPELES + SALDO_PLAN_PPTAL.MODIF_INGRESOS_PAPELES)
               END
          END PENDIENTECOBRO,
          PLAN_PRESUPUESTAL.DESTINO, 
          PLAN_PRESUPUESTAL.NIVEL1, 
          PLAN_PRESUPUESTAL.NIVEL2, 
          PLAN_PRESUPUESTAL.NIVEL3, 
          PLAN_PRESUPUESTAL.NIVEL4, 
          PLAN_PRESUPUESTAL.NIVEL5, 
          PLAN_PRESUPUESTAL.NIVEL6, 
          PLAN_PRESUPUESTAL.AUXILIAR, 
          PLAN_PRESUPUESTAL.CENTRO_COSTO
    FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL 
        INNER JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL 
                ON PLAN_PRESUPUESTAL.ID       = SALDO_PLAN_PPTAL.ID 
               AND PLAN_PRESUPUESTAL.ANO      = SALDO_PLAN_PPTAL.ANO 
               AND PLAN_PRESUPUESTAL.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
    WHERE SALDO_PLAN_PPTAL.COMPANIA = s$compania$s 
      AND SALDO_PLAN_PPTAL.ID BETWEEN 's$cuentaInicial$s'  AND  's$cuentaFinal$s'
      AND SALDO_PLAN_PPTAL.ANO      = s$anio$s
      AND SALDO_PLAN_PPTAL.MES BETWEEN 0 AND s$mes$s
      AND LENGTH(SALDO_PLAN_PPTAL.ID) BETWEEN 0 AND NVL(s$nivel$s,6) 
      AND PLAN_PRESUPUESTAL.INFORME NOT IN (0) 
      AND PLAN_PRESUPUESTAL.NATURALEZA = 'C'
      s$condicion$s
    GROUP BY SALDO_PLAN_PPTAL.COMPANIA, 
             SALDO_PLAN_PPTAL.ANO,
             SALDO_PLAN_PPTAL.ID, 
             PLAN_PRESUPUESTAL.NOMBRE, 
             PLAN_PRESUPUESTAL.DESTINO, 
             PLAN_PRESUPUESTAL.NIVEL1, 
             PLAN_PRESUPUESTAL.NIVEL2, 
             PLAN_PRESUPUESTAL.NIVEL3, 
             PLAN_PRESUPUESTAL.NIVEL4, 
             PLAN_PRESUPUESTAL.NIVEL5,
             PLAN_PRESUPUESTAL.NIVEL6,
             PLAN_PRESUPUESTAL.AUXILIAR, 
             PLAN_PRESUPUESTAL.CENTRO_COSTO, 
             PLAN_PRESUPUESTAL.NATURALEZA
 )
  SELECT FC_REGISTROEJECUCINGRESOS_036.CUENTA,
         FC_REGISTROEJECUCINGRESOS_036.APROPIACIONVIGENTE, 
         FC_REGISTROEJECUCINGRESOS_036.DERECHOSACUM,  
         FC_REGISTROEJECUCINGRESOS_036.EFECTIVOACUM, 
         FC_REGISTROEJECUCINGRESOS_036.PAPELESACUM,
         FC_REGISTROEJECUCINGRESOS_036.PENDIENTECOBRO, 
         FC_REGISTROEJECUCINGRESOS_036.DESTINO,
         FC_REGISTROEJECUCINGRESOS_036.NIVEL1, 
         FC_REGISTROEJECUCINGRESOS_036.NIVEL2, 
         FC_REGISTROEJECUCINGRESOS_036.NIVEL3,
         FC_REGISTROEJECUCINGRESOS_036.NIVEL4,
         FC_REGISTROEJECUCINGRESOS_036.NIVEL5,
         FC_REGISTROEJECUCINGRESOS_036.NIVEL6,
         FC_REGISTROEJECUCINGRESOS_036.NOMBRE,
         CASE WHEN s$enMiles$s NOT IN (0) 
              THEN ROUND(SALDO_PLAN_PPTAL.INGRESOS_CAUSADOS + SALDO_PLAN_PPTAL.MODIF_INGRESOS_CAUSADOS,-3)/1000 
              ELSE SALDO_PLAN_PPTAL.INGRESOS_CAUSADOS + SALDO_PLAN_PPTAL.MODIF_INGRESOS_CAUSADOS
         END DERECHOSMES, 
         CASE WHEN s$enMiles$s NOT IN (0) 
              THEN ROUND(SALDO_PLAN_PPTAL.INGRESOS_EFECTIVO + SALDO_PLAN_PPTAL.MODIF_INGRESOS_EFECTIVO,-3)/1000 
              ELSE SALDO_PLAN_PPTAL.INGRESOS_EFECTIVO + SALDO_PLAN_PPTAL.MODIF_INGRESOS_EFECTIVO
         END EFECTIVOMES, 
         CASE WHEN s$enMiles$s NOT IN (0) 
              THEN ROUND(SALDO_PLAN_PPTAL.INGRESOS_PAPELES + SALDO_PLAN_PPTAL.MODIF_INGRESOS_PAPELES,-3)/1000 
              ELSE SALDO_PLAN_PPTAL.INGRESOS_PAPELES + SALDO_PLAN_PPTAL.MODIF_INGRESOS_PAPELES 
         END PAPELESMES, 
         SALDO_PLAN_PPTAL.MES, 
         LPAD(FC_REGISTROEJECUCINGRESOS_036.CUENTA,1) AS CLASE, 
         FC_REGISTROEJECUCINGRESOS_036.AUXILIAR, 
         FC_REGISTROEJECUCINGRESOS_036.CENTRO_COSTO
  FROM FC_REGISTROEJECUCINGRESOS_036 
        LEFT JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL 
               ON FC_REGISTROEJECUCINGRESOS_036.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
              AND FC_REGISTROEJECUCINGRESOS_036.ANO      = SALDO_PLAN_PPTAL.ANO 
              AND FC_REGISTROEJECUCINGRESOS_036.CUENTA   = SALDO_PLAN_PPTAL.ID
  WHERE SALDO_PLAN_PPTAL.MES = s$mes$s
  ORDER BY FC_REGISTROEJECUCINGRESOS_036.COMPANIA, 
           FC_REGISTROEJECUCINGRESOS_036.ANO, 
           FC_REGISTROEJECUCINGRESOS_036.CUENTA
