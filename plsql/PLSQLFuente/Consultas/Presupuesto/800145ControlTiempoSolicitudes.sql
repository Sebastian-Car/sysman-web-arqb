SELECT SOLICITUDDISPONIBILIDAD.NUMERO,
  TO_CHAR(SOLICITUD.FECHA,'DD/MM/YYYY') FECHA_SOLICITUD,
  TERCERO.NOMBRE NOMBRE_SOLICITANTE,
  SOLICITUDDISPONIBILIDAD.VALOR VALOR_SOLICITUD,
  CASE
    WHEN SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD = '1'
    THEN 'X'
    ELSE ''
  END NUEVO,
  CASE
    WHEN SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD = '2'
    THEN 'X'
    ELSE ''
  END INCREMENTO,
  CASE
    WHEN SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD = '3'
    THEN 'X'
    ELSE ''
  END DECREMENTO,
  TO_CHAR(
  CASE
    WHEN TO_NUMBER(TO_CHAR(SOLICITUDDISPONIBILIDAD.FECHA,'HH24')) >= 15
    THEN SOLICITUDDISPONIBILIDAD.FECHA + NUMTODSINTERVAL(4,'DAY')
    ELSE SOLICITUDDISPONIBILIDAD.FECHA + NUMTODSINTERVAL(3,'DAY')
  END,'DD/MM/YYYY') FECHA_LIMITE_EXPEDICION,
  SOLICITUDDISPONIBILIDAD.NRO_SOLICITUD_AFECT NO_CDP,
  TO_CHAR(SOLICITUDDISPONIBILIDAD.FECHA,'DD/MM/YYYY') FECHA_EXPEDICION,
  PCK_SYSMAN_UTL.FC_EDAD(SOLICITUD.FECHA,SOLICITUDDISPONIBILIDAD.FECHA,3) TIEMPO_DE_RESPUESTA
FROM SOLICITUDDISPONIBILIDAD
LEFT JOIN SOLICITUDDISPONIBILIDAD SOLICITUD
ON SOLICITUD.COMPANIA        = SOLICITUDDISPONIBILIDAD.COMPANIA
AND SOLICITUD.NUMERO         = SOLICITUDDISPONIBILIDAD.NRO_SOLICITUD_AFECT
AND SOLICITUD.TIPO_SOLICITUD = SOLICITUDDISPONIBILIDAD.TIPO_CPTE_AFECT
INNER JOIN TERCERO
ON SOLICITUDDISPONIBILIDAD.COMPANIA    = TERCERO.COMPANIA
AND SOLICITUDDISPONIBILIDAD.TERCERO    = TERCERO.NIT
AND SOLICITUDDISPONIBILIDAD.SUCURSAL   = TERCERO.SUCURSAL
WHERE SOLICITUDDISPONIBILIDAD.COMPANIA = s$compania$s
AND SOLICITUDDISPONIBILIDAD.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s
