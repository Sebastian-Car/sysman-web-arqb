SELECT  V_RESUMENPPTO_BASE.ID CODIGO,
        V_RESUMENPPTO_BASE.NOMBRE,
        SUM(V_RESUMENPPTO_BASE.APROPIADO) APROPIADO,
        SUM(V_RESUMENPPTO_BASE.ADICION + V_RESUMENPPTO_BASE.REDUCCION + V_RESUMENPPTO_BASE.TRASLADO + V_RESUMENPPTO_BASE.APLAZAMIENTO) MODIFICACIONES,
        SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE) PPTODEFINITIVO,
        SUM(V_RESUMENPPTO_BASE.PACTOTAL) PACTOTAL,
        SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE - (V_RESUMENPPTO_BASE.PACTOTAL))SALDOPPTOSEGUNPAC,
        SUM(V_RESUMENPPTO_BASE.TOTALICA) INGRESOSCAUSADOS,
        SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT)  EJECUCIONPPT,
        SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE - (V_RESUMENPPTO_BASE.TOTALICA))SALDOPPTONOCAUSADO,
        SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE - V_RESUMENPPTO_BASE.EJECUCIONPPT) SALDOPPTOEFECTIVO,
        SUM(( V_RESUMENPPTO_BASE.PACTOTAL)-(  V_RESUMENPPTO_BASE.TOTALICA)) DEFICITOEXCEDENTEPAC,
        ABS(SUM(( V_RESUMENPPTO_BASE.TOTALICA)-(V_RESUMENPPTO_BASE.EJECUCIONPPT))) RENTASPORCOBRAR
FROM  V_RESUMENPPTO_BASE 
WHERE V_RESUMENPPTO_BASE.COMPANIA = 's$compania$s'
  AND V_RESUMENPPTO_BASE.ANO = s$ano$s
  AND V_RESUMENPPTO_BASE.MES BETWEEN 0 AND s$mes$s 
  AND V_RESUMENPPTO_BASE.ID BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  AND V_RESUMENPPTO_BASE.NATURALEZA IN('s$naturaleza$s')
 GROUP BY V_RESUMENPPTO_BASE.ID, V_RESUMENPPTO_BASE.NOMBRE
