WITH LISEJECUCIONINGRESOS_CTRECE 
AS 
(
SELECT SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,
  SALDO_PLAN_PPTAL.ID CUENTA,
  PLAN_PRESUPUESTAL.MAN_CEN_CTO,
  SUM(
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
    THEN APROPIACION_DEBITO -APROPIACION_CREDITO
    ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
  END) APROPIADO,
  SUM(
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
    THEN APLAZAM_DEBITO -APLAZAM_CREDITO
    ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
  END) APLAZAMIENTO,
  SUM(
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='C'
    THEN APLAZAM_CREDITO
  END) DESAPLAZAMIENTO,
  SUM(
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='C'
    THEN APLAZAM_DEBITO
  END) APLAZAMIENTOS,
  SUM(
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
    THEN TRASLADO_DEBITO -TRASLADO_CREDITO
    ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
  END) TRASLADO,
  SUM(SALDO_PLAN_PPTAL.ADICION) ADICIONA,
  SUM(SALDO_PLAN_PPTAL.REDUCCION) REDUCCIONA,
  PLAN_PRESUPUESTAL.NOMBRE,
  (SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
    THEN TRASLADO_DEBITO -TRASLADO_CREDITO
    ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
  END)+SUM(SALDO_PLAN_PPTAL.REDUCCION)+SUM(
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
    THEN APLAZAM_DEBITO -APLAZAM_CREDITO
    ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
  END)) MODIFICACIONES,
  SUM(
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
    THEN APROPIACION_DEBITO -APROPIACION_CREDITO
    ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
  END)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.REDUCCION)+SUM(
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
    THEN TRASLADO_DEBITO -TRASLADO_CREDITO
    ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
  END)+SUM(
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
    THEN APLAZAM_DEBITO -APLAZAM_CREDITO
    ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
  END) TOTALPRESUPUESTO,
  SUM(
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
    THEN APROPIACION_DEBITO-APROPIACION_CREDITO
    ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
  END)+SUM(SALDO_PLAN_PPTAL.ADICION)-SUM(SALDO_PLAN_PPTAL.REDUCCION) TOTALPRESUPUESTOS,
  SUM((
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
    THEN EJE_PPT_DEBITO -EJE_PPT_CREDITO
    ELSE EJE_PPT_CREDITO-EJE_PPT_DEBITO
  END)+MODIF_INGRESOS) RECAUDOSACUMULADOS,
  SUM(
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
    THEN APROPIACION_DEBITO -APROPIACION_CREDITO
    ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
  END)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.REDUCCION)+SUM(
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
    THEN TRASLADO_DEBITO -TRASLADO_CREDITO
    ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
  END)+SUM(
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
    THEN APLAZAM_DEBITO -APLAZAM_CREDITO
    ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
  END)                  -SUM((
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
    THEN EJE_PPT_DEBITO -EJE_PPT_CREDITO
    ELSE EJE_PPT_CREDITO-EJE_PPT_DEBITO
  END)+MODIF_INGRESOS) PORRECAUDAR,
  SUM(
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
    THEN APROPIACION_DEBITO -APROPIACION_CREDITO
    ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
  END)+SUM(SALDO_PLAN_PPTAL.ADICION)-SUM(SALDO_PLAN_PPTAL.REDUCCION)-SUM((
  CASE
    WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
    THEN EJE_PPT_DEBITO -EJE_PPT_CREDITO
    ELSE EJE_PPT_CREDITO-EJE_PPT_DEBITO
  END)+MODIF_INGRESOS) PORRECAUDARS,
  CASE
    WHEN (SUM(
      CASE
        WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
        THEN APROPIACION_DEBITO -APROPIACION_CREDITO
        ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
      END)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.REDUCCION)+SUM(
      CASE
        WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
        THEN TRASLADO_DEBITO -TRASLADO_CREDITO
        ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
      END)+SUM(
      CASE
        WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
        THEN APLAZAM_DEBITO -APLAZAM_CREDITO
        ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
      END))>0
    THEN (SUM((
      CASE
        WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
        THEN EJE_PPT_DEBITO -EJE_PPT_CREDITO
        ELSE EJE_PPT_CREDITO-EJE_PPT_DEBITO
      END)+MODIF_INGRESOS)/(SUM(
      CASE
        WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
        THEN APROPIACION_DEBITO -APROPIACION_CREDITO
        ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
      END)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.REDUCCION)+SUM(
      CASE
        WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
        THEN TRASLADO_DEBITO -TRASLADO_CREDITO
        ELSE TRASLADO_CREDITO-TRASLADO_DEBITO
      END)+SUM(
      CASE
        WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
        THEN APLAZAM_DEBITO -APLAZAM_CREDITO
        ELSE APLAZAM_CREDITO-APLAZAM_DEBITO
      END)))*100
    ELSE 0
  END PORCRECAUDADO,
  SUM(TRASLADO_DEBITO +APLAZAM_DEBITO) CREDITOS, 
  SUM(TRASLADO_CREDITO+APLAZAM_CREDITO) CONTRACREDITOS,
  SUM(TRASLADO_CREDITO+APLAZAM_CREDITO) CONTRACREDITOSINI,
  SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO) CREDITOSS,
  SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO) CONTRACREDITOSINIS,
  SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO) CONTRACREDITOSS,
  PLAN_PRESUPUESTAL.CONSITUACIONFONDOS,
  PLAN_PRESUPUESTAL.FUENTE_RECURSO FUENTE,
  PLAN_PRESUPUESTAL.DESTINO
FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
 INNER JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
  ON  PLAN_PRESUPUESTAL.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
  AND PLAN_PRESUPUESTAL.ANO      = SALDO_PLAN_PPTAL.ANO
  AND PLAN_PRESUPUESTAL.ID   = SALDO_PLAN_PPTAL.ID  
WHERE SALDO_PLAN_PPTAL.COMPANIA  = s$compania$s
AND SALDO_PLAN_PPTAL.ANO         = s$anio$s
AND PLAN_PRESUPUESTAL.NATURALEZA ='C'
AND SALDO_PLAN_PPTAL.MES         < s$mesInicial$s  
AND SALDO_PLAN_PPTAL.CODIGO        BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
AND PLAN_PRESUPUESTAL.AUXILIAR     IS NULL
AND PLAN_PRESUPUESTAL.CENTRO_COSTO IS NULL
GROUP BY SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,  
  SALDO_PLAN_PPTAL.ID, 
  PLAN_PRESUPUESTAL.MAN_CEN_CTO, 
  PLAN_PRESUPUESTAL.NATURALEZA,   
  PLAN_PRESUPUESTAL.NOMBRE,
  PLAN_PRESUPUESTAL.CONSITUACIONFONDOS,
  PLAN_PRESUPUESTAL.FUENTE_RECURSO,
  PLAN_PRESUPUESTAL.DESTINO,
  PLAN_PRESUPUESTAL.AUXILIAR,
  PLAN_PRESUPUESTAL.CENTRO_COSTO)
SELECT 
  LISEJECUCIONINGRESOS_CTRECE.COMPANIA,
  LISEJECUCIONINGRESOS_CTRECE.ANO,
  SALDO_PLAN_PPTAL.MES,
  SALDO_PLAN_PPTAL.ADICION ADICIONMES,
  LISEJECUCIONINGRESOS_CTRECE.CUENTA,
  LISEJECUCIONINGRESOS_CTRECE.NOMBRE,
  LISEJECUCIONINGRESOS_CTRECE.APROPIADO+(LISEJECUCIONINGRESOS_CTRECE.ADICIONA+SALDO_PLAN_PPTAL.ADICION)+(LISEJECUCIONINGRESOS_CTRECE.REDUCCIONA+SALDO_PLAN_PPTAL.REDUCCION) TOTALPRESUPUESTOF, 
  LISEJECUCIONINGRESOS_CTRECE.RECAUDOSACUMULADOS+(EJE_PPT_CREDITO-EJE_PPT_DEBITO+MODIF_INGRESOS_EFECTIVO+MODIF_INGRESOS_PAPELES) TOTALRECAUDOS,
  LISEJECUCIONINGRESOS_CTRECE.MODIFICACIONES+SALDO_PLAN_PPTAL.ADICION+SALDO_PLAN_PPTAL.REDUCCION+TRASLADO_CREDITO-TRASLADO_DEBITO  MODIFICACIONESA, 
  (LISEJECUCIONINGRESOS_CTRECE.APROPIADO+(LISEJECUCIONINGRESOS_CTRECE.ADICIONA+SALDO_PLAN_PPTAL.ADICION)+(LISEJECUCIONINGRESOS_CTRECE.REDUCCIONA+SALDO_PLAN_PPTAL.REDUCCION))-LISEJECUCIONINGRESOS_CTRECE.RECAUDOSACUMULADOS+(SALDO_PLAN_PPTAL.EJE_PPT_CREDITO-SALDO_PLAN_PPTAL.EJE_PPT_DEBITO+SALDO_PLAN_PPTAL.MODIF_INGRESOS_EFECTIVO+SALDO_PLAN_PPTAL.MODIF_INGRESOS_PAPELES) PORECAUDAR,
  CASE WHEN LISEJECUCIONINGRESOS_CTRECE.APROPIADO+(LISEJECUCIONINGRESOS_CTRECE.ADICIONA+SALDO_PLAN_PPTAL.ADICION)+(LISEJECUCIONINGRESOS_CTRECE.REDUCCIONA+SALDO_PLAN_PPTAL.REDUCCION)=0
       THEN 0
       ELSE ((LISEJECUCIONINGRESOS_CTRECE.RECAUDOSACUMULADOS+(EJE_PPT_CREDITO-EJE_PPT_DEBITO+MODIF_INGRESOS_EFECTIVO+MODIF_INGRESOS_PAPELES))/(LISEJECUCIONINGRESOS_CTRECE.APROPIADO+(LISEJECUCIONINGRESOS_CTRECE.ADICIONA+SALDO_PLAN_PPTAL.ADICION)+(LISEJECUCIONINGRESOS_CTRECE.REDUCCIONA+SALDO_PLAN_PPTAL.REDUCCION)))*100
       END POREJECUCION,
  SALDO_PLAN_PPTAL.EJE_PPT_CREDITO-SALDO_PLAN_PPTAL.EJE_PPT_DEBITO+SALDO_PLAN_PPTAL.MODIF_INGRESOS_EFECTIVO+SALDO_PLAN_PPTAL.MODIF_INGRESOS_PAPELES RECAUDOSMES,
  SALDO_PLAN_PPTAL.REDUCCION REDUCCIONMES,
  LISEJECUCIONINGRESOS_CTRECE.ADICIONA+SALDO_PLAN_PPTAL.ADICION TOTALADICIONES,
  SALDO_PLAN_PPTAL.ADICION+SALDO_PLAN_PPTAL.REDUCCION+TRASLADO_CREDITO-TRASLADO_DEBITO TOTALMODIFICACIONES,
  LISEJECUCIONINGRESOS_CTRECE.REDUCCIONA+SALDO_PLAN_PPTAL.REDUCCION TOTALREDUCCIONES,
  TRASLADO_CREDITO-TRASLADO_DEBITO TRASLADOMES, 
  LISEJECUCIONINGRESOS_CTRECE.APROPIADO
FROM LISEJECUCIONINGRESOS_CTRECE
 INNER JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
  ON  LISEJECUCIONINGRESOS_CTRECE.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
  AND LISEJECUCIONINGRESOS_CTRECE.ANO      = SALDO_PLAN_PPTAL.ANO
  AND LISEJECUCIONINGRESOS_CTRECE.CUENTA   = SALDO_PLAN_PPTAL.ID
WHERE SALDO_PLAN_PPTAL.MES = s$mesInicial$s
