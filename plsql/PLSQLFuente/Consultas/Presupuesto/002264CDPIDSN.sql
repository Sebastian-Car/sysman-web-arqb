SELECT DISTINCT
COMPROBANTE_PPTAL.TIPO TIPO_CPTE,
    COMPROBANTE_PPTAL.NUMERO COMPROBANTE,
    COMPROBANTE_PPTAL.TERCERO,
    COMPROBANTE_PPTAL.SUCURSAL,
    TIPO_COMPROBPP.NOMBRE NOMBRETIPO,
    TERCERO.NOMBRE NOMBRETERCERO,
    COMPROBANTE_PPTAL.DESCRIPCION,
    (DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT || '-' || DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO || ' ' || COMPROBANTE_PPTAL.DESCRIPCION) TOTALDESCRIPCION,
    DETALLE_COMPROBANTE_PPTAL.CUENTA CODIGO_CUENTA, 
    DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO CODAUXILIAR,
    PLAN_PRESUPUESTAL.NOMBRE 
    || (CASE WHEN MAN_CEN_CTO = 0 THEN '' ELSE ' / ' || CENTRO_COSTO.NOMBRE END)
    || (CASE WHEN MAN_AUX_TER = 0 THEN '' ELSE ' / ' || TERCERO.NOMBRE END)
    || (CASE WHEN MAN_AUX_GEN = 0 THEN '' ELSE ' / ' || AUXILIAR.NOMBRE END) 
    || (CASE WHEN MAN_AUX_REF = 0 THEN '' ELSE ' / ' || REFERENCIA.NOMBRE END)
    || (CASE WHEN MAN_AUX_FUE = 0 THEN '' ELSE ' / ' || FUENTE_RECURSOS.NOMBRE END) AS NOMBRETOTAL,
    DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO,
    DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO,
    TO_CHAR(COMPROBANTE_PPTAL.FECHA_VCN_DOC , 'DD/MM/YYYY') FECHA_VCN_DOC,
    TO_CHAR(COMPROBANTE_PPTAL.FECHA,'DD\MM\YYYY') FECHA,
    DETALLE_COMPROBANTE_PPTAL.MODIFIED_BY,
    DETALLE_COMPROBANTE_PPTAL.AUXILIAR,
    DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO,
    DETALLE_COMPROBANTE_PPTAL.REFERENCIA
FROM COMPROBANTE_PPTAL
INNER JOIN DETALLE_COMPROBANTE_PPTAL
    ON COMPROBANTE_PPTAL.COMPANIA       = DETALLE_COMPROBANTE_PPTAL.COMPANIA
    AND COMPROBANTE_PPTAL.ANO = DETALLE_COMPROBANTE_PPTAL.ANO
    AND COMPROBANTE_PPTAL.TIPO = DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE
    AND COMPROBANTE_PPTAL.NUMERO = DETALLE_COMPROBANTE_PPTAL.COMPROBANTE
INNER JOIN TIPO_COMPROBPP
    ON COMPROBANTE_PPTAL.TIPO      = TIPO_COMPROBPP.CODIGO
    AND COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA
INNER JOIN TERCERO 
    ON DETALLE_COMPROBANTE_PPTAL.SUCURSAL  = TERCERO.SUCURSAL
    AND DETALLE_COMPROBANTE_PPTAL.TERCERO  = TERCERO.NIT
    AND DETALLE_COMPROBANTE_PPTAL.COMPANIA = TERCERO.COMPANIA
INNER JOIN AUXILIAR
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA  = AUXILIAR.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO  = AUXILIAR.ANO
    AND DETALLE_COMPROBANTE_PPTAL.AUXILIAR = AUXILIAR.CODIGO
INNER JOIN CENTRO_COSTO
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA      = CENTRO_COSTO.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO      = CENTRO_COSTO.ANO
    AND DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO = CENTRO_COSTO.CODIGO
INNER JOIN PLAN_PRESUPUESTAL
    ON DETALLE_COMPROBANTE_PPTAL.CUENTA = PLAN_PRESUPUESTAL.CODIGO
    AND DETALLE_COMPROBANTE_PPTAL.ANO          = PLAN_PRESUPUESTAL.ANO
    AND DETALLE_COMPROBANTE_PPTAL.COMPANIA     = PLAN_PRESUPUESTAL.COMPANIA
INNER JOIN FUENTE_RECURSOS 
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA           = FUENTE_RECURSOS.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO            = FUENTE_RECURSOS.ANO
    AND DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO = FUENTE_RECURSOS.CODIGO
INNER JOIN REFERENCIA 
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA       = REFERENCIA.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO        = REFERENCIA.ANO
    AND DETALLE_COMPROBANTE_PPTAL.REFERENCIA = REFERENCIA.CODIGO 
LEFT JOIN V_DETALLE_AUXILIAR_PPTAL
    ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = V_DETALLE_AUXILIAR_PPTAL.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = V_DETALLE_AUXILIAR_PPTAL.COMPROBANTE
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE =  V_DETALLE_AUXILIAR_PPTAL.TIPO
    AND DETALLE_COMPROBANTE_PPTAL.ANO = V_DETALLE_AUXILIAR_PPTAL.ANO
WHERE COMPROBANTE_PPTAL.COMPANIA = s$compania$s 
    AND COMPROBANTE_PPTAL.ANO    = s$ano$s 
    AND COMPROBANTE_PPTAL.TIPO   = 's$tipoCpte$s' 
   AND COMPROBANTE_PPTAL.NUMERO BETWEEN 's$numeroPptoInicial$s' AND 's$numeroPptoFinal$s'