WITH Mes_TRINI AS
  (SELECT V_RESUMENPPTO_BASE.COMPANIA,
    V_RESUMENPPTO_BASE.MES,
    V_RESUMENPPTO_BASE.ANO,
    V_RESUMENPPTO_BASE.ID CUENTA,
    MesANT_TRINI.EjecucionPptMesANT,
    SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT+V_RESUMENPPTO_BASE.TOTALMODIFINGRESOS) AS EjecucionPptMes
  FROM V_RESUMENPPTO_BASE
  LEFT JOIN V_SALDO_PLAN_PPTAL
  ON V_RESUMENPPTO_BASE.ID        = V_SALDO_PLAN_PPTAL.ID
  AND V_RESUMENPPTO_BASE.ANO      = V_SALDO_PLAN_PPTAL.ANO
  AND V_RESUMENPPTO_BASE.COMPANIA = V_SALDO_PLAN_PPTAL.COMPANIA
  LEFT JOIN
    (SELECT V_RESUMENPPTO_BASE.COMPANIA,
      V_RESUMENPPTO_BASE.MES,
      V_RESUMENPPTO_BASE.ANO,
      V_RESUMENPPTO_BASE.ID CUENTA,
      SUM(
      CASE
        WHEN V_RESUMENPPTO_BASE.Naturaleza='D'
        THEN V_SALDO_PLAN_PPTAL.Eje_ppt_Debito  -V_SALDO_PLAN_PPTAL.Eje_ppt_Credito
        ELSE (V_SALDO_PLAN_PPTAL.Eje_ppt_Credito-V_SALDO_PLAN_PPTAL.Eje_ppt_Debito)+V_RESUMENPPTO_BASE.TOTALMODIFINGRESOS
      END) AS EjecucionPptMesANT,
      SUM(
      CASE
        WHEN V_RESUMENPPTO_BASE.Naturaleza='D'
        THEN V_SALDO_PLAN_PPTAL.Eje_ppt_Debito  -V_SALDO_PLAN_PPTAL.Eje_ppt_Credito
        ELSE (V_SALDO_PLAN_PPTAL.Eje_ppt_Credito-V_SALDO_PLAN_PPTAL.Eje_ppt_Debito)+V_RESUMENPPTO_BASE.TOTALMODIFINGRESOS
      END) AS EjecucionPptMes
    FROM V_RESUMENPPTO_BASE
    LEFT JOIN V_SALDO_PLAN_PPTAL
    ON V_RESUMENPPTO_BASE.ID          = V_SALDO_PLAN_PPTAL.ID
    AND V_RESUMENPPTO_BASE.ANO        = V_SALDO_PLAN_PPTAL.ANO
    AND V_RESUMENPPTO_BASE.COMPANIA   = V_SALDO_PLAN_PPTAL.COMPANIA
    WHERE V_RESUMENPPTO_BASE.COMPANIA = s$compania$s
    AND V_RESUMENPPTO_BASE.ANO        = 's$ano$s'
    AND V_RESUMENPPTO_BASE.MES        =s$mes$s -1
    AND V_RESUMENPPTO_BASE.NATURALEZA ='C'
    AND V_RESUMENPPTO_BASE.ID BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
    GROUP BY V_RESUMENPPTO_BASE.COMPANIA,
      V_RESUMENPPTO_BASE.MES,
      V_RESUMENPPTO_BASE.ANO,
      V_RESUMENPPTO_BASE.ID
    ) MesANT_TRINI ON V_RESUMENPPTO_BASE.COMPANIA = MesANT_TRINI.COMPANIA
  AND V_RESUMENPPTO_BASE.ANO                      = MesANT_TRINI.ANO
  AND V_RESUMENPPTO_BASE.ID                       = MesANT_TRINI.CUENTA
  WHERE V_RESUMENPPTO_BASE.COMPANIA               =s$compania$s
  AND V_RESUMENPPTO_BASE.ANO                      ='s$ano$s'
  AND V_RESUMENPPTO_BASE.MES                      =s$mes$s
  AND V_RESUMENPPTO_BASE.ID BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
  AND V_RESUMENPPTO_BASE.NATURALEZA='C'
  GROUP BY V_RESUMENPPTO_BASE.COMPANIA,
    V_RESUMENPPTO_BASE.MES,
    V_RESUMENPPTO_BASE.ANO,
    V_RESUMENPPTO_BASE.ID,
    MesANT_TRINI.EjecucionPptMesANT
  )
SELECT V_RESUMENPPTO_BASE.COMPANIA,
  V_RESUMENPPTO_BASE.ANO,
  V_RESUMENPPTO_BASE.ID CUENTA,
  SUBSTR(V_RESUMENPPTO_BASE.ID,s$mes$s,s$mes$s) AS CLASE,
  SUM(V_RESUMENPPTO_BASE.TRASLADO_DEBITO)       AS CreditoF,
  SUM(V_RESUMENPPTO_BASE.TRASLADO_CREDITO)      AS ContraCreditoF,
  SUM(V_RESUMENPPTO_BASE.APROPIADO)             AS ApropiadoF,
  Mes_TRINI.EjecucionPptMesANT,
  Mes_TRINI.EjecucionPptMes,
  Mes_TRINI.EjecucionPptMesANT      +Mes_TRINI.EjecucionPptMes                AS EjecucionPptF,
  SUM(V_RESUMENPPTO_BASE.ADICION)   +SUM(V_RESUMENPPTO_BASE.Traslado_Debito)  AS ModifAdicionF,
  SUM(V_RESUMENPPTO_BASE.REDUCCION) +SUM(V_RESUMENPPTO_BASE.TRASLADO_CREDITO) AS ModifReduccionF,
  V_RESUMENPPTO_BASE.NATURALEZA,
  V_RESUMENPPTO_BASE.NOMBRE,
  SUM(V_RESUMENPPTO_BASE.APROPIADO) +SUM(V_RESUMENPPTO_BASE.ADICION) +SUM(V_RESUMENPPTO_BASE.TRASLADO)+SUM(V_RESUMENPPTO_BASE.REDUCCION)                                                                             AS PtoDefinitivoF,
  SUM(V_RESUMENPPTO_BASE.APROPIADO) +SUM(V_RESUMENPPTO_BASE.ADICION) +SUM(V_RESUMENPPTO_BASE.TRASLADO)+SUM(V_RESUMENPPTO_BASE.REDUCCION)-SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT +V_RESUMENPPTO_BASE.TOTALMODIFINGRESOS) AS SaldoPorRecaudarF
FROM V_RESUMENPPTO_BASE
LEFT JOIN Mes_TRINI
ON V_RESUMENPPTO_BASE.COMPANIA    = Mes_TRINI.COMPANIA
AND V_RESUMENPPTO_BASE.ANO        = Mes_TRINI.ANO
AND V_RESUMENPPTO_BASE.ID         = Mes_TRINI.CUENTA
WHERE V_RESUMENPPTO_BASE.COMPANIA = s$compania$s
AND V_RESUMENPPTO_BASE.ANO        ='s$ano$s'
AND V_RESUMENPPTO_BASE.MES BETWEEN 0 AND s$mes$s
AND V_RESUMENPPTO_BASE.NATURALEZA ='C'
AND V_RESUMENPPTO_BASE.ID BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
GROUP BY V_RESUMENPPTO_BASE.COMPANIA,
  V_RESUMENPPTO_BASE.ANO,
  V_RESUMENPPTO_BASE.ID,
  Mes_TRINI.EjecucionPptMesANT,
  Mes_TRINI.EjecucionPptMes,
  V_RESUMENPPTO_BASE.NATURALEZA,
  V_RESUMENPPTO_BASE.CODIGO,
  V_RESUMENPPTO_BASE.NOMBRE
