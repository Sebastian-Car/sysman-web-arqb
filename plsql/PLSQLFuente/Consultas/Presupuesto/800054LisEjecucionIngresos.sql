WITH LISEJECUCIONINGRESOS_GUA AS (
    SELECT  V_SALDO_PLAN_PPTAL.COMPANIA,
            V_SALDO_PLAN_PPTAL.ANO,
            V_SALDO_PLAN_PPTAL.ID CUENTA,
            V_PLAN_PRESUPUESTAL.CODIGO,
            V_PLAN_PRESUPUESTAL.NOMBRE,
            TO_CHAR(V_PLAN_PRESUPUESTAL.MAN_CEN_CTO) MOVIMIENTO,
            SUM(CASE WHEN V_PLAN_PRESUPUESTAL.NATURALEZA='D' 
                  THEN V_SALDO_PLAN_PPTAL.APROPIACION_DEBITO - V_SALDO_PLAN_PPTAL.APROPIACION_CREDITO 
                  ELSE V_SALDO_PLAN_PPTAL.APROPIACION_CREDITO -V_SALDO_PLAN_PPTAL.APROPIACION_DEBITO 
            END) AS APROPIADO,
            SUM(V_SALDO_PLAN_PPTAL.ADICION) AS ADICIONA,
            SUM(V_SALDO_PLAN_PPTAL.REDUCCION)AS REDUCCIONA,
            SUM(V_SALDO_PLAN_PPTAL.INGRESOS_CAUSADOS + (V_SALDO_PLAN_PPTAL.MODIF_INGRESOS_CAUSADOS))AS CAUSACIONACUMULADA,
            SUM(CASE WHEN V_PLAN_PRESUPUESTAL.NATURALEZA ='D' 
                  THEN V_SALDO_PLAN_PPTAL.EJE_PPT_DEBITO - V_SALDO_PLAN_PPTAL.EJE_PPT_CREDITO 
                  ELSE (V_SALDO_PLAN_PPTAL.EJE_PPT_CREDITO - V_SALDO_PLAN_PPTAL.EJE_PPT_DEBITO)+ V_SALDO_PLAN_PPTAL.MODIF_INGRESOS 
            END) AS RECAUDOSACUMULADOS,
            V_PLAN_PRESUPUESTAL.CONSITUACIONFONDOS,
            V_PLAN_PRESUPUESTAL.FUENTE_RECURSO,
            V_PLAN_PRESUPUESTAL.DESTINO
    FROM V_PLAN_PRESUPUESTAL
      INNER JOIN V_SALDO_PLAN_PPTAL
        ON V_PLAN_PRESUPUESTAL.COMPANIA = V_SALDO_PLAN_PPTAL.COMPANIA 
        AND V_PLAN_PRESUPUESTAL.ANO     = V_SALDO_PLAN_PPTAL.ANO
        AND V_PLAN_PRESUPUESTAL.ID      = V_SALDO_PLAN_PPTAL.ID
    WHERE V_SALDO_PLAN_PPTAL.COMPANIA = s$compania$s
      AND V_SALDO_PLAN_PPTAL.ANO = s$ano$s
      AND V_SALDO_PLAN_PPTAL.MES   < s$mesFinal$s
      AND V_SALDO_PLAN_PPTAL.ID BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s     
      AND V_PLAN_PRESUPUESTAL.NATURALEZA = 'C'
      AND V_PLAN_PRESUPUESTAL.AUXILIAR  IS NULL
      AND V_PLAN_PRESUPUESTAL.CENTRO_COSTO IS NULL
    GROUP BY  V_SALDO_PLAN_PPTAL.COMPANIA,
              V_SALDO_PLAN_PPTAL.ANO,
              V_SALDO_PLAN_PPTAL.ID,
              V_PLAN_PRESUPUESTAL.CODIGO,
              V_PLAN_PRESUPUESTAL.NOMBRE,
              V_PLAN_PRESUPUESTAL.MAN_CEN_CTO ,
              V_PLAN_PRESUPUESTAL.CONSITUACIONFONDOS,
              V_PLAN_PRESUPUESTAL.FUENTE_RECURSO,
              V_PLAN_PRESUPUESTAL.DESTINO)
SELECT 
       LISEJECUCIONINGRESOS_GUA.CODIGO,
       LISEJECUCIONINGRESOS_GUA.NOMBRE,
       LISEJECUCIONINGRESOS_GUA.MOVIMIENTO ,
       LISEJECUCIONINGRESOS_GUA.APROPIADO,
       LISEJECUCIONINGRESOS_GUA.ADICIONA ADICIONES_ANTERIORES,
       V_SALDO_PLAN_PPTAL.ADICION AS ADICIONES_MES,
       LISEJECUCIONINGRESOS_GUA.ADICIONA + (V_SALDO_PLAN_PPTAL.ADICION) AS TOTAL_ADICIONES,
       LISEJECUCIONINGRESOS_GUA.REDUCCIONA  AS REDUCCIONES_ANTERIORES,
       V_SALDO_PLAN_PPTAL.REDUCCION AS REDUCCIONES_MES,
       LISEJECUCIONINGRESOS_GUA.REDUCCIONA  +  V_SALDO_PLAN_PPTAL.REDUCCION AS TOTAL_REDUCCIONES,
   LISEJECUCIONINGRESOS_GUA.APROPIADO +  (LISEJECUCIONINGRESOS_GUA.ADICIONA + (V_SALDO_PLAN_PPTAL.ADICION)) + 
        LISEJECUCIONINGRESOS_GUA.REDUCCIONA  +  V_SALDO_PLAN_PPTAL.REDUCCION  AS TOTAL_PRESUPUESTO,
       LISEJECUCIONINGRESOS_GUA.CAUSACIONACUMULADA + (INGRESOS_CAUSADOS +(MODIF_INGRESOS_CAUSADOS))AS TOTAL_RECONOCIMIENTOS,
       INGRESOS_CAUSADOS +(MODIF_INGRESOS_CAUSADOS)  AS RECONOCIMIENTO_MES,
       LISEJECUCIONINGRESOS_GUA.CAUSACIONACUMULADA RECONOCIMIENTOS_ANTERIORES, 
       LISEJECUCIONINGRESOS_GUA.RECAUDOSACUMULADOS + 
       EJE_PPT_CREDITO - EJE_PPT_DEBITO + MODIF_INGRESOS_EFECTIVO + MODIF_INGRESOS_PAPELES AS TOTAL_RECAUDOS,
       EJE_PPT_CREDITO - EJE_PPT_DEBITO + MODIF_INGRESOS_EFECTIVO + MODIF_INGRESOS_PAPELES AS RECAUDOS_MES, 
       LISEJECUCIONINGRESOS_GUA.RECAUDOSACUMULADOS AS RECAUDOS_ANTERIORES,
       (LISEJECUCIONINGRESOS_GUA.APROPIADO  + 
      (LISEJECUCIONINGRESOS_GUA.ADICIONA + (V_SALDO_PLAN_PPTAL.ADICION)) + 
      (LISEJECUCIONINGRESOS_GUA.REDUCCIONA + (V_SALDO_PLAN_PPTAL.REDUCCION)))-
      (LISEJECUCIONINGRESOS_GUA.RECAUDOSACUMULADOS +  EJE_PPT_CREDITO - EJE_PPT_DEBITO + MODIF_INGRESOS_EFECTIVO + MODIF_INGRESOS_PAPELES ) SALDO_POR_RECAUDAR,
       LISEJECUCIONINGRESOS_GUA.CAUSACIONACUMULADA - LISEJECUCIONINGRESOS_GUA.RECAUDOSACUMULADOS AS CUENTAS_POR_COBRAR,
       LISEJECUCIONINGRESOS_GUA.CONSITUACIONFONDOS,
       LISEJECUCIONINGRESOS_GUA.FUENTE_RECURSO FUENTE,
       LISEJECUCIONINGRESOS_GUA.DESTINO   
FROM LISEJECUCIONINGRESOS_GUA
  INNER JOIN V_SALDO_PLAN_PPTAL
    ON LISEJECUCIONINGRESOS_GUA.COMPANIA = V_SALDO_PLAN_PPTAL.COMPANIA 
    AND LISEJECUCIONINGRESOS_GUA.ANO      = V_SALDO_PLAN_PPTAL.ANO
    AND LISEJECUCIONINGRESOS_GUA.CUENTA    = V_SALDO_PLAN_PPTAL.ID 
WHERE V_SALDO_PLAN_PPTAL.MES          = s$mesFinal$s
ORDER BY  LISEJECUCIONINGRESOS_GUA.COMPANIA,
          LISEJECUCIONINGRESOS_GUA.ANO,
          LISEJECUCIONINGRESOS_GUA.CUENTA
