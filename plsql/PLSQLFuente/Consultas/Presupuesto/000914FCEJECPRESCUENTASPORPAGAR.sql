WITH APROPIACIONINCIAL AS (
      SELECT  PLAN_PRESUPUESTAL.ID ID1,
            SALDO_PLAN_PPTAL.COMPANIA  COMPANIA1,
            SALDO_PLAN_PPTAL.ANO  ANO1,
            SALDO_PLAN_PPTAL.ID  CUENTA1,
            SUM(CASE WHEN 1 = s$miles$s 
                  THEN ROUND((SALDO_PLAN_PPTAL.APROPIACION_DEBITO - SALDO_PLAN_PPTAL.APROPIACION_CREDITO),-3)/1000
                  ELSE (SALDO_PLAN_PPTAL.APROPIACION_DEBITO - SALDO_PLAN_PPTAL.APROPIACION_CREDITO)
                END) CXPCONSTITUIDAS,
            SUM(CASE WHEN 1 = s$miles$s
                  THEN ROUND((SALDO_PLAN_PPTAL.EJE_PPT_DEBITO - SALDO_PLAN_PPTAL.EJE_PPT_CREDITO),-3)/1000
                  ELSE (SALDO_PLAN_PPTAL.EJE_PPT_DEBITO - SALDO_PLAN_PPTAL.EJE_PPT_CREDITO) 
                END)PAGOSACUMA
    FROM V_PLAN_PRESUPUESTAL  PLAN_PRESUPUESTAL
      INNER JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
        ON  PLAN_PRESUPUESTAL.COMPANIA        = SALDO_PLAN_PPTAL.COMPANIA
        AND PLAN_PRESUPUESTAL.ANO             = SALDO_PLAN_PPTAL.ANO
        AND PLAN_PRESUPUESTAL.CODIGO              = SALDO_PLAN_PPTAL.CODIGO
    WHERE  SALDO_PLAN_PPTAL.COMPANIA        = s$compania$s
      AND SALDO_PLAN_PPTAL.ANO              = s$ano$s
      AND SALDO_PLAN_PPTAL.MES  BETWEEN 0 AND s$mesInicial$s - 1 
      AND SALDO_PLAN_PPTAL.ID BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s 
      AND PLAN_PRESUPUESTAL.TIPOVIGENCIA IN ('RC')
      AND PLAN_PRESUPUESTAL.NATURALEZA   IN ('D')
      AND LENGTH(SALDO_PLAN_PPTAL.ID) BETWEEN 0 AND NVL(s$nivel$s,6)
	  s$condicion$s
    GROUP BY  PLAN_PRESUPUESTAL.ID,
              SALDO_PLAN_PPTAL.COMPANIA,
              SALDO_PLAN_PPTAL.ANO,
              SALDO_PLAN_PPTAL.ID
    ORDER BY PLAN_PRESUPUESTAL.ID
                        ),
 APROPIACIONESACTUALES AS (
        SELECT  SUBSTR(SALDO_PLAN_PPTAL.ID,1,1) AS CLASE,
              PLAN_PRESUPUESTAL.ID,
              PLAN_PRESUPUESTAL.NOMBRE,
              SALDO_PLAN_PPTAL.COMPANIA,
              SALDO_PLAN_PPTAL.ANO,
              SALDO_PLAN_PPTAL.ID CUENTA,
              s$centroCosto$s 
              s$auxiliar$s
              SUM(CASE WHEN 1 = s$miles$s 
                    THEN ROUND(SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION,-3)/1000
                    ELSE SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION
                    END)CANCELACIONES,
              SUM(CASE WHEN 1 = s$miles$s
	           THEN ROUND(SALDO_PLAN_PPTAL.EJE_PPT_DEBITO - SALDO_PLAN_PPTAL.EJE_PPT_CREDITO,-3)/1000 
	           ELSE  SALDO_PLAN_PPTAL.EJE_PPT_DEBITO - SALDO_PLAN_PPTAL.EJE_PPT_CREDITO END) PAGOSACUMP
      FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
        LEFT JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
          ON PLAN_PRESUPUESTAL.COMPANIA     = SALDO_PLAN_PPTAL.COMPANIA
          AND PLAN_PRESUPUESTAL.ANO         = SALDO_PLAN_PPTAL.ANO
          AND PLAN_PRESUPUESTAL.CODIGO          = SALDO_PLAN_PPTAL.CODIGO
      WHERE SALDO_PLAN_PPTAL.COMPANIA       = s$compania$s
        AND SALDO_PLAN_PPTAL.ANO            = s$ano$s
        AND SALDO_PLAN_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s
        AND SALDO_PLAN_PPTAL.ID BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s 
        AND PLAN_PRESUPUESTAL.TIPOVIGENCIA = 'RC'
        AND PLAN_PRESUPUESTAL.NATURALEZA   = 'D'
        AND LENGTH(SALDO_PLAN_PPTAL.ID) BETWEEN 0 AND NVL(s$nivel$s,6)
      GROUP BY  SUBSTR(SALDO_PLAN_PPTAL.ID,1,1),
                PLAN_PRESUPUESTAL.ID,
                SALDO_PLAN_PPTAL.COMPANIA,
                SALDO_PLAN_PPTAL.ANO,
                SALDO_PLAN_PPTAL.ID,
                s$agrupacionC$s
                s$agrupacionA$s
                PLAN_PRESUPUESTAL.NOMBRE
        )  
SELECT 	
        APROPIACIONESACTUALES.COMPANIA,
        APROPIACIONESACTUALES.ID,
        APROPIACIONESACTUALES.NOMBRE,
        APROPIACIONESACTUALES.CUENTA,
        APROPIACIONINCIAL.CXPCONSTITUIDAS,
        APROPIACIONESACTUALES.CANCELACIONES,
        APROPIACIONINCIAL.CXPCONSTITUIDAS + APROPIACIONESACTUALES.CANCELACIONES AS CXPDEF,
        APROPIACIONINCIAL.PAGOSACUMA  + APROPIACIONESACTUALES.PAGOSACUMP    AS PAGOS,
        APROPIACIONINCIAL.CXPCONSTITUIDAS + APROPIACIONESACTUALES.CANCELACIONES - (APROPIACIONINCIAL.PAGOSACUMA  + APROPIACIONESACTUALES.PAGOSACUMP)   AS SALDOXP,
        APROPIACIONESACTUALES.CLASE
FROM APROPIACIONINCIAL
	LEFT JOIN APROPIACIONESACTUALES
		ON APROPIACIONINCIAL.COMPANIA1 = APROPIACIONESACTUALES.COMPANIA 
		AND APROPIACIONINCIAL.ANO1     = APROPIACIONESACTUALES.ANO	
		AND APROPIACIONINCIAL.ID1      = APROPIACIONESACTUALES.ID
WHERE 	APROPIACIONESACTUALES.COMPANIA = s$compania$s	
s$condicionFinal$s
