WITH RUBROS_NEGATIVOS AS
(
    SELECT SALDO_PLAN_PPTAL.COMPANIA,
           SALDO_PLAN_PPTAL.ANO,
           SALDO_PLAN_PPTAL.CODIGO,
           PLAN_PRESUPUESTAL.NOMBRE,
           SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA = 'D' THEN APROPIACION_DEBITO - APROPIACION_CREDITO ELSE APROPIACION_CREDITO - APROPIACION_DEBITO END) + 
           SUM(SALDO_PLAN_PPTAL.ADICION) +  
           SUM(SALDO_PLAN_PPTAL.REDUCCION) +
           SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA = 'D' THEN TRASLADO_DEBITO - TRASLADO_CREDITO ELSE TRASLADO_CREDITO - TRASLADO_DEBITO END) AS APRDEFINITIVA, 
           SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA = 'D' THEN TRASLADO_DEBITO - TRASLADO_CREDITO       ELSE TRASLADO_CREDITO - TRASLADO_DEBITO END) AS TRASLADO,
           SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD) AS DISPONIBILIDAD,
           SUM(SALDO_PLAN_PPTAL.REG_CONTRACT) + SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT) AS REGCONT,
           (SUM(SALDO_PLAN_PPTAL.REG_CONTRACT) + SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)) + (SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)+ SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT)) AS TOTALREG,
            SUM(SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION) + SUM(SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION) AS OBLIGACIONESACUM
    FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL 
        INNER JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL 
              ON PLAN_PRESUPUESTAL.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
             AND PLAN_PRESUPUESTAL.ANO          = SALDO_PLAN_PPTAL.ANO
             AND PLAN_PRESUPUESTAL.ID             = SALDO_PLAN_PPTAL.ID
    WHERE SALDO_PLAN_PPTAL.COMPANIA = s$compania$s
         AND SALDO_PLAN_PPTAL.ANO      = s$anio$s
         AND SALDO_PLAN_PPTAL.MES      <= 12
          AND SALDO_PLAN_PPTAL.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
    GROUP BY SALDO_PLAN_PPTAL.COMPANIA, 
             SALDO_PLAN_PPTAL.ANO,
             SALDO_PLAN_PPTAL.CODIGO, 
             PLAN_PRESUPUESTAL.NOMBRE 
  )
SELECT COMPANIA,
       ANO,
       CODIGO CUENTA,
       NOMBRE, 
       APRDEFINITIVA,
       DISPONIBILIDAD,
       APRDEFINITIVA - DISPONIBILIDAD AS SALDOANUALDISPONIBLE,
       TOTALREG,
       DISPONIBILIDAD - TOTALREG AS SALDODISPONIBILIDADES, 
       OBLIGACIONESACUM,
       TOTALREG-OBLIGACIONESACUM AS SALDOREGISTRO
FROM RUBROS_NEGATIVOS
WHERE ROUND(APRDEFINITIVA     -  DISPONIBILIDAD,2)      <0
       OR ROUND(DISPONIBILIDAD   -  TOTALREG,2)                <0
       OR ROUND(TOTALREG             - OBLIGACIONESACUM,2)<0
