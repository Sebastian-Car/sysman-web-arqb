WITH LISEJECUCIONGASTOS_PAUXILIARES AS (
SELECT SALDO_PLAN_PPTAL.COMPANIA COMPANIA,
  SALDO_PLAN_PPTAL.ANO ANO,
  PLAN_PRESUPUESTAL.NOMBRE,
  SUM(APROPIACION_DEBITO-APROPIACION_CREDITO ) APROPIADO,
  SUM(SALDO_PLAN_PPTAL.ADICION)  ADICION,
  SUM(SALDO_PLAN_PPTAL.REDUCCION)  REDUCCION,
  SUM(TRASLADO_DEBITO-TRASLADO_CREDITO)  TRASLADO,
  SUM(APLAZAM_DEBITO-APLAZAM_CREDITO)    APLAZAMIENTO,
  (SUM(APROPIACION_DEBITO-APROPIACION_CREDITO)+SUM(SALDO_PLAN_PPTAL.ADICION) +SUM(SALDO_PLAN_PPTAL.REDUCCION) +SUM(TRASLADO_DEBITO-TRASLADO_CREDITO)+SUM(APLAZAM_DEBITO-APLAZAM_CREDITO))  TOTALAPROPIADO,
  SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD)     DISPONIBILIDADES,
  SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)       REGCONT,
  SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)    REGNOCONT,
  SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)     MODREGCONT,
  SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT)   MODREGNOCONT,
  SUM(REGISTRO_OBLIGACION+MODIF_REGISTRO_OBLIGACION)          REGISTROOBLIGACION,
  SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)+SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT)                                                                      AS RESERVAS,
  SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD)-(SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)+SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT))                          TOTAL,
  SUM(EJE_PPT_DEBITO-EJE_PPT_CREDITO)  PAGOSACUMULADOS,
  (SUM(APROPIACION_DEBITO-APROPIACION_CREDITO)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.REDUCCION) +SUM(TRASLADO_DEBITO-TRASLADO_CREDITO)+SUM(APLAZAM_DEBITO-APLAZAM_CREDITO))-(SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD)-(SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)+SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT))) SALDO,
  PLAN_PRESUPUESTAL.MOVIMIENTO,
  PLAN_PRESUPUESTAL.ID,
  SALDO_PLAN_PPTAL.CODIGO,
  (SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.REDUCCION)+SUM(TRASLADO_DEBITO-TRASLADO_CREDITO)+SUM(APLAZAM_DEBITO-APLAZAM_CREDITO)) MODIFICACIONES,
  SUM(REGISTRO_OBLIGACION+MODIF_REGISTRO_OBLIGACION)- SUM(EJE_PPT_DEBITO-EJE_PPT_CREDITO)   CUENTASPORPAGAR,
  ( CASE WHEN   (SUM(APROPIACION_DEBITO-APROPIACION_CREDITO)+SUM(SALDO_PLAN_PPTAL.ADICION) +SUM(SALDO_PLAN_PPTAL.REDUCCION) +SUM(TRASLADO_DEBITO-TRASLADO_CREDITO)+SUM(APLAZAM_DEBITO-APLAZAM_CREDITO))>0 THEN ((SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)+SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT) )/  (SUM(APROPIACION_DEBITO-APROPIACION_CREDITO)+SUM(SALDO_PLAN_PPTAL.ADICION) +SUM(SALDO_PLAN_PPTAL.REDUCCION) +SUM(TRASLADO_DEBITO-TRASLADO_CREDITO)+SUM(APLAZAM_DEBITO-APLAZAM_CREDITO)))*100 ELSE 0 END ) COMPR,
  SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD) -(SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)+SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT))   SALDODEDISPONIBILIDADES,
  (SUM(APROPIACION_DEBITO-APROPIACION_CREDITO )+SUM(SALDO_PLAN_PPTAL.ADICION)  +SUM(SALDO_PLAN_PPTAL.REDUCCION)+SUM(TRASLADO_DEBITO-TRASLADO_CREDITO)+SUM(APLAZAM_DEBITO-APLAZAM_CREDITO))  -(SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)+SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT))   SALDODEAPROPIACION,
  SUM(TRASLADO_DEBITO+APLAZAM_DEBITO)  CREDITOS,
  SUM(TRASLADO_CREDITO+APLAZAM_CREDITO)  CONTRACREDITOSINI,
  SUM(TRASLADO_CREDITO+APLAZAM_CREDITO)+ABS(SUM(SALDO_PLAN_PPTAL.REDUCCION)) CONTRACREDITOS
 FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
  LEFT JOIN V_SALDO_PLAN_PPTAL  SALDO_PLAN_PPTAL
  ON PLAN_PRESUPUESTAL.COMPANIA   = SALDO_PLAN_PPTAL.COMPANIA
  AND PLAN_PRESUPUESTAL.ANO       = SALDO_PLAN_PPTAL.ANO
  AND PLAN_PRESUPUESTAL.ID        = SALDO_PLAN_PPTAL.ID
WHERE SALDO_PLAN_PPTAL.COMPANIA= 's$compania$s'
AND SALDO_PLAN_PPTAL.ANO   =  s$anio$s
AND SALDO_PLAN_PPTAL.MES BETWEEN 0 AND s$mes$s
AND PLAN_PRESUPUESTAL.NATURALEZA = 'D'
AND SALDO_PLAN_PPTAL.ID BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'

GROUP BY SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,
  PLAN_PRESUPUESTAL.NOMBRE,
  PLAN_PRESUPUESTAL.MOVIMIENTO,
  SALDO_PLAN_PPTAL.CODIGO,
  PLAN_PRESUPUESTAL.MOVIMIENTO,
  PLAN_PRESUPUESTAL.ID,
  PLAN_PRESUPUESTAL.NATURALEZA
HAVING  (SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD)   <> 0
AND SUM(EJE_PPT_DEBITO-EJE_PPT_CREDITO) <> 0)
) 
SELECT 
  LISEJECUCIONGASTOS_PAUXILIARES.ID CUENTA,
  LISEJECUCIONGASTOS_PAUXILIARES.NOMBRE,
  LISEJECUCIONGASTOS_PAUXILIARES.RESERVAS    RESERVASACUM,
  LISEJECUCIONGASTOS_PAUXILIARES.REGISTROOBLIGACION REGISACUM,
  (SALDO_PLAN_PPTAL.EJE_PPT_DEBITO-SALDO_PLAN_PPTAL.EJE_PPT_CREDITO)             PAGOSMES,
  LISEJECUCIONGASTOS_PAUXILIARES.PAGOSACUMULADOS  -((SALDO_PLAN_PPTAL.EJE_PPT_DEBITO-SALDO_PLAN_PPTAL.EJE_PPT_CREDITO))  PAGOSANTERIORES,
  LISEJECUCIONGASTOS_PAUXILIARES.COMPANIA,
  LISEJECUCIONGASTOS_PAUXILIARES.TOTALAPROPIADO,
  LISEJECUCIONGASTOS_PAUXILIARES.APROPIADO,
  LISEJECUCIONGASTOS_PAUXILIARES.ADICION,
  LISEJECUCIONGASTOS_PAUXILIARES.PAGOSACUMULADOS,
  LISEJECUCIONGASTOS_PAUXILIARES.MOVIMIENTO,
  LISEJECUCIONGASTOS_PAUXILIARES.DISPONIBILIDADES,
  LISEJECUCIONGASTOS_PAUXILIARES.CREDITOS,
  LISEJECUCIONGASTOS_PAUXILIARES.CONTRACREDITOS,
  SALDO_PLAN_PPTAL.DISPONIBILIDAD    DISPONIBILIDADMES,
  SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION+SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION REGISTROOBLIGACIONMES,
  SALDO_PLAN_PPTAL.REG_CONTRACT+SALDO_PLAN_PPTAL.REG_NO_CONTRACT+SALDO_PLAN_PPTAL.MODIF_REG_CONT+SALDO_PLAN_PPTAL.MODIF_REG_NOCONT  RESERVASMES
FROM LISEJECUCIONGASTOS_PAUXILIARES
INNER JOIN SALDO_PLAN_PPTAL
ON LISEJECUCIONGASTOS_PAUXILIARES.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
AND LISEJECUCIONGASTOS_PAUXILIARES.ANO     = SALDO_PLAN_PPTAL.ANO
AND LISEJECUCIONGASTOS_PAUXILIARES.CODIGO  = SALDO_PLAN_PPTAL.CODIGO
WHERE SALDO_PLAN_PPTAL.MES             = s$mes$s
ORDER BY LISEJECUCIONGASTOS_PAUXILIARES.ID
