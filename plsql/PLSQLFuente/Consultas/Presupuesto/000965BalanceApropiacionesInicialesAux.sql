SELECT
  PLAN_PRESUPUESTAL.COMPANIA ,
  PLAN_PRESUPUESTAL.ANO ,
  PLAN_PRESUPUESTAL.CODIGO ,
  NULL TERCERO,
  NULL SUCURSAL,
  NULL AUXILIAR,
  NULL CENTRO_COSTO,
  NULL REFERENCIA,
  NULL FUENTE_RECURSO,
  SUM(APROPIACIONESINICIALES.APROPIACIONINICIAL) APROPIACIONINICIAL ,
  SUM(APROPIACIONESINICIALES.DEBITO) DEBITO,
  SUM(APROPIACIONESINICIALES.CREDITO) CREDITO ,
  PLAN_PRESUPUESTAL.NOMBRE,
  NULL NITCOMPLETO,
  SUBSTR(PLAN_PRESUPUESTAL.CODIGO,0,1) GRUPO
FROM APROPIACIONESINICIALES 
  INNER JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL 
     ON APROPIACIONESINICIALES.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA 
    AND APROPIACIONESINICIALES.ANO           = PLAN_PRESUPUESTAL.ANO 
    AND SUBSTR(APROPIACIONESINICIALES.CODIGO,1,LENGTH(PLAN_PRESUPUESTAL.ID))              = PLAN_PRESUPUESTAL.ID 
WHERE APROPIACIONESINICIALES.COMPANIA =  s$compania$s
  AND APROPIACIONESINICIALES.ANO =  s$anio$s 
GROUP BY PLAN_PRESUPUESTAL.COMPANIA, 
         PLAN_PRESUPUESTAL.ANO, 
         PLAN_PRESUPUESTAL.CODIGO, 
         PLAN_PRESUPUESTAL.NOMBRE,
         NULL
UNION ALL 
SELECT
  APROPIACIONESINICIALES.COMPANIA ,
  APROPIACIONESINICIALES.ANO ,
  APROPIACIONESINICIALES.CODIGO ,
  CASE WHEN APROPIACIONESINICIALES.TERCERO = PCK_DATOS.FC_CONS_TERCERO 
       THEN ''
       ELSE APROPIACIONESINICIALES.TERCERO
  END TERCERO,
  CASE WHEN APROPIACIONESINICIALES.SUCURSAL = PCK_DATOS.FC_CONS_SUCURSAL 
       THEN ''
       ELSE APROPIACIONESINICIALES.SUCURSAL
  END SUCURSAL,
  CASE WHEN APROPIACIONESINICIALES.AUXILIAR = PCK_DATOS.FC_CONS_AUXILIAR 
       THEN ''
       ELSE APROPIACIONESINICIALES.AUXILIAR
  END AUXILIAR,
  CASE WHEN APROPIACIONESINICIALES.CENTRO_COSTO = PCK_DATOS.FC_CONS_CENTRO 
       THEN ''
       ELSE APROPIACIONESINICIALES.CENTRO_COSTO
  END CENTRO_COSTO,
  CASE WHEN APROPIACIONESINICIALES.REFERENCIA = PCK_DATOS.FC_CONS_REFERENCIA 
       THEN ''
       ELSE APROPIACIONESINICIALES.REFERENCIA
  END REFERENCIA,
  CASE WHEN APROPIACIONESINICIALES.FUENTE_RECURSO = PCK_DATOS.FC_CONS_FUENTE 
       THEN ''
       ELSE APROPIACIONESINICIALES.FUENTE_RECURSO
  END FUENTE_RECURSO,
  APROPIACIONESINICIALES.APROPIACIONINICIAL ,
  APROPIACIONESINICIALES.DEBITO ,
  APROPIACIONESINICIALES.CREDITO ,
  PLAN_PRESUPUESTAL.NOMBRE,
  CASE WHEN APROPIACIONESINICIALES.TERCERO = PCK_DATOS.FC_CONS_TERCERO 
       THEN ''
       ELSE APROPIACIONESINICIALES.TERCERO
  END NITCOMPLETO,
  SUBSTR(APROPIACIONESINICIALES.ID,0,1) GRUPO
FROM APROPIACIONESINICIALES 
  INNER JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL 
     ON APROPIACIONESINICIALES.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA 
    AND APROPIACIONESINICIALES.ANO           = PLAN_PRESUPUESTAL.ANO 
    AND APROPIACIONESINICIALES.ID              = PLAN_PRESUPUESTAL.ID 
WHERE APROPIACIONESINICIALES.COMPANIA = s$compania$s
  AND APROPIACIONESINICIALES.ANO = s$anio$s
