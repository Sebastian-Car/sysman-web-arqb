SELECT 
  TIPO_COMPROBPP.COMPANIA,
  TIPO_COMPROBPP.CODIGO,   
  UPPER(TIPO_COMPROBPP.NOMBRE) TIPO_COMPROBPP_NOM, 
  DETALLE_COMPROBANTE_PPTAL.ID  IDCTA,
  DETALLE_COMPROBANTE_PPTAL.CUENTA CTA, 
  ABS(SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
               THEN VALOR_DEBITO-VALOR_CREDITO
               ELSE VALOR_CREDITO-VALOR_DEBITO
               END)) VALORTIPO,
  CASE WHEN PLAN_PRESUPUESTAL.NOMBRE IS NULL 
       THEN '  ' 
       ELSE PLAN_PRESUPUESTAL.NOMBRE
  END NOMBRECTA  
FROM DETALLE_COMPROBANTE_PPTAL 
 INNER JOIN TIPO_COMPROBPP
   ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA 
  AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
 LEFT JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL   
   ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
  AND DETALLE_COMPROBANTE_PPTAL.ANO       = PLAN_PRESUPUESTAL.ANO
  AND DETALLE_COMPROBANTE_PPTAL.ID        = PLAN_PRESUPUESTAL.ID 
WHERE TIPO_COMPROBPP.COMPANIA          = s$compania$s 
  AND DETALLE_COMPROBANTE_PPTAL.ANO    = s$anio$s
    AND DETALLE_COMPROBANTE_PPTAL.ID BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s' 
GROUP BY TIPO_COMPROBPP.COMPANIA, 
  TIPO_COMPROBPP.CODIGO, 
  TIPO_COMPROBPP.NOMBRE,   
  DETALLE_COMPROBANTE_PPTAL.ANO, 
  DETALLE_COMPROBANTE_PPTAL.ID, 
  PLAN_PRESUPUESTAL.NOMBRE,
  DETALLE_COMPROBANTE_PPTAL.CUENTA
