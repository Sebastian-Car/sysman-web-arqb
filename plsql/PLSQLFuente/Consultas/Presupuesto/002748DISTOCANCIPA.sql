  COMPROBANTE_PPTAL.TERCERO TERCERONIT,
  VISTA.FECHA_COM,
  TERCERO.NOMBRE TERCERO_NOM,
  COMPROBANTE_PPTAL.SUCURSAL,
  VISTA.CUENTANOMBRE TOTALNOMBRE,
  VISTA.VALOR_DEBITO,
