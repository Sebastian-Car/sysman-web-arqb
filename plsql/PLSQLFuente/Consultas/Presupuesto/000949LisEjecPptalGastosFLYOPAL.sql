SELECT    V_PLAN_PRESUPUESTAL.COMPANIA,
                V_PLAN_PRESUPUESTAL.ANO,
                V_PLAN_PRESUPUESTAL.CODIGO,
                V_PLAN_PRESUPUESTAL.ID,
                CASE WHEN V_PLAN_PRESUPUESTAL.FUENTE_RECURSO IS NULL 
                          THEN NULL
                          ELSE V_PLAN_PRESUPUESTAL.FUENTE_RECURSO
                END AUXILIAR,
                V_PLAN_PRESUPUESTAL.NOMBRE,
                TRUNC(SUM(V_RESUMENPPTO_BASE.APROPIADO)* POWER(10,s$digitos$s)) / POWER(10,s$digitos$s) APROPIADO,
                TRUNC(SUM(V_RESUMENPPTO_BASE.ADICION  + V_RESUMENPPTO_BASE.REDUCCION +
                          V_RESUMENPPTO_BASE.TRASLADO + V_RESUMENPPTO_BASE.APLAZAMIENTO)* POWER(10,s$digitos$s)) / POWER(10,s$digitos$s) MODIFICACIONES,
                TRUNC(SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE)* POWER(10,s$digitos$s)) / POWER(10,s$digitos$s)TOTALAPROPIADO,
                TRUNC(SUM(V_RESUMENPPTO_BASE.DISPONIBILIDAD)* POWER(10,s$digitos$s)) / POWER(10,s$digitos$s) AS DISPONIBILIDADES,
                TRUNC(SUM(V_RESUMENPPTO_BASE.COMPROMISOSACUM)* POWER(10,s$digitos$s)) / POWER(10,s$digitos$s)RESERVAS,
                TRUNC(SUM(V_RESUMENPPTO_BASE.OBLIGACIONESACUM)* POWER(10,s$digitos$s)) / POWER(10,s$digitos$s) AS REGISTROOBLIGACION,
                TRUNC(SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT)* POWER(10,s$digitos$s)) / POWER(10,s$digitos$s)  AS PAGOSACUMULADOS,
                TRUNC(SUM(V_RESUMENPPTO_BASE.COMPROMISOSACUM - V_RESUMENPPTO_BASE.EJECUCIONPPT)* POWER(10,s$digitos$s)) / POWER(10,s$digitos$s)  SALDORES,
                TRUNC(SUM(V_RESUMENPPTO_BASE.OBLIGACIONESACUM - V_RESUMENPPTO_BASE.EJECUCIONPPT)* POWER(10,s$digitos$s)) / POWER(10,s$digitos$s) CUENTASPORPAGAR1,
                TRUNC(SUM(V_RESUMENPPTO_BASE.DISPONIBILIDAD - V_RESUMENPPTO_BASE.COMPROMISOSACUM)* POWER(10,s$digitos$s)) / POWER(10,s$digitos$s) TOTAL1,
                TRUNC(SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT - CASE WHEN V_RESUMENPPTO_BASE.MES = s$mes$s THEN V_RESUMENPPTO_BASE.EJECUCIONPPT ELSE 0 END)* POWER(10,s$digitos$s)) / POWER(10,s$digitos$s)PAGOSANTERIORES,
                TRUNC(SUM(CASE WHEN V_RESUMENPPTO_BASE.MES = s$mes$s THEN V_RESUMENPPTO_BASE.EJECUCIONPPT ELSE 0 END)* POWER(10,s$digitos$s)) / POWER(10,s$digitos$s) PAGOSMES,
                TRUNC(SUM(CASE WHEN V_RESUMENPPTO_BASE.MES = s$mes$s THEN V_RESUMENPPTO_BASE.COMPROMISOSACUM ELSE 0 END)* POWER(10,s$digitos$s)) / POWER(10,s$digitos$s)COMPROMISOSMES
FROM V_RESUMENPPTO_BASE
  INNER JOIN V_PLAN_PRESUPUESTAL
    ON V_RESUMENPPTO_BASE.COMPANIA = V_PLAN_PRESUPUESTAL.COMPANIA 
    AND V_RESUMENPPTO_BASE.ANO          = V_PLAN_PRESUPUESTAL.ANO
    AND SUBSTR(V_RESUMENPPTO_BASE.ID,0, LENGTH(V_PLAN_PRESUPUESTAL.ID))     = V_PLAN_PRESUPUESTAL.ID 
WHERE V_RESUMENPPTO_BASE.COMPANIA  = s$compania$s
  AND V_RESUMENPPTO_BASE.ANO = s$ano$s
  AND V_RESUMENPPTO_BASE.ID BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
  AND V_RESUMENPPTO_BASE.MES  <= s$mes$s 
  AND V_RESUMENPPTO_BASE.NATURALEZA = 'D'          
  AND V_RESUMENPPTO_BASE.AUXILIAR BETWEEN s$fuenteInicial$s AND s$fuenteFinal$s
  AND LENGTH(V_RESUMENPPTO_BASE.CODIGO) BETWEEN 0 AND NVL(s$nivel$s,6)
  AND V_RESUMENPPTO_BASE.FUENTE_RECURSO  NOT IN ('99999999999999999999')
GROUP BY  V_PLAN_PRESUPUESTAL.COMPANIA,
          V_PLAN_PRESUPUESTAL.ANO,
          V_PLAN_PRESUPUESTAL.CODIGO,
          V_PLAN_PRESUPUESTAL.ID, 
          V_PLAN_PRESUPUESTAL.NOMBRE,
          CASE WHEN V_PLAN_PRESUPUESTAL.FUENTE_RECURSO IS NULL 
               THEN NULL
               ELSE V_PLAN_PRESUPUESTAL.FUENTE_RECURSO
               END
