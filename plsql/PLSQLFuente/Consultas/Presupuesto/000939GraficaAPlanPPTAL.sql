SELECT
  DECODE(V_SALDO_PLAN_PPTAL.MES,0,'Ini',1,'Ene',2,'Feb',3,'Mar',4,'Abr',5,'May',6,'Jun',7,'Jul',8,'Ago',9,'Sep',10,'Oct',11,'Nov',12,'Dic',13,'FIN') EXPR1,
  (CASE WHEN 's$naturaleza$s'='D' THEN APROPIACION_DEBITO-APROPIACION_CREDITO ELSE APROPIACION_CREDITO-APROPIACION_DEBITO END)+V_SALDO_PLAN_PPTAL.ADICION+V_SALDO_PLAN_PPTAL.REDUCCION+(CASE WHEN 's$naturaleza$s'='D' THEN TRASLADO_DEBITO-TRASLADO_CREDITO ELSE TRASLADO_CREDITO-TRASLADO_DEBITO END)+(CASE WHEN 's$naturaleza$s'='D' THEN APLAZAM_DEBITO-APLAZAM_CREDITO ELSE APLAZAM_CREDITO-APLAZAM_DEBITO END) APROPIACION,
  (V_SALDO_PLAN_PPTAL.REG_CONTRACT+V_SALDO_PLAN_PPTAL.REG_NO_CONTRACT+V_SALDO_PLAN_PPTAL.MODIF_REG_CONT+V_SALDO_PLAN_PPTAL.MODIF_REG_NOCONT) REGISTRO,
  V_SALDO_PLAN_PPTAL.DISPONIBILIDAD DISPONIBILIDAD, 
  CASE WHEN 's$naturaleza$s'='D' THEN EJE_PPT_DEBITO-EJE_PPT_CREDITO ELSE EJE_PPT_CREDITO-EJE_PPT_DEBITO END EJECUCION
FROM V_SALDO_PLAN_PPTAL
WHERE V_SALDO_PLAN_PPTAL.COMPANIA = s$compania$s
  AND V_SALDO_PLAN_PPTAL.ANO = s$anio$s
  AND V_SALDO_PLAN_PPTAL.ID = 's$codigo$s'
