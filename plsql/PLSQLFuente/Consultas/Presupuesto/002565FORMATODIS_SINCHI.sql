MERGE INTO CONSULTAS FIN USING (SELECT '002565FORMATODIS_SINCHI' INFORME ,TO_CLOB(q'[SELECT SOL.TIPO_SOLICITUD,
SOL.RECURSO,
REFERENCIA.NOMBRE AS NOMBRE_REFERENCIA,
TER2.NOMBRE AS RESPONSABLE_APROBAR,
TER.NOMBRE AS BENEFICIARIO,
D_SOL.RUBRO,
PLAN.NOMBRE,
SOL.NUMERO,
SOL.FECHA,
TO_CHAR(SOL.FECHA, 'DD') DIA,
TO_CHAR(SOL.FECHA, 'MM') MES,
TO_CHAR(SOL.FECHA, 'YYYY') ANO,
TERCERO.NOMBRE AS TERCERO,
SOL.OBJETO,
D_SOL.VALOR,
DEP.NOMBRE AS NOMBREDEPENDENCIA,
SOL.CIUDAD,
CASE WHEN PLAN.MAN_CEN_CTO <> 0 THEN D_SOL.CENTRO_COSTO ELSE '' END CENTRO_COSTO,
CASE WHEN PLAN.MAN_AUX_REF <> 0 THEN D_SOL.REFERENCIA ELSE '' END REFERENCIA,
CASE WHEN PLAN.MAN_AUX_FUE <> 0 THEN D_SOL.FUENTE ELSE '' END FUENTE,
CASE WHEN PLAN.MAN_AUX_GEN <> 0 THEN D_SOL.AUXILIAR ELSE '' END AUXILIAR
FROM SOLICITUDDISPONIBILIDAD SOL
INNER JOIN D_SOLICITUDDISPONIBILIDAD D_SOL
ON SOL.COMPANIA = D_SOL.COMPANIA
AND SOL.NUMERO = D_SOL.NUMERO
AND SOL.TIPO_SOLICITUD = D_SOL.TIPO_SOLICITUD
INNER JOIN PLAN_PRESUPUESTAL PLAN
ON D_SOL.COMPANIA = PLAN.COMPANIA
AND D_SOL.ANO = PLAN.ANO
AND D_SOL.RUBRO = PLAN.CODIGO
LEFT JOIN DEPENDENCIA DEP
ON SOL.COMPANIA = DEP.COMPANIA
AND SOL.DEPENDENCIA = DEP.CODIGO
LEFT JOIN TERCERO
ON SOL.COMPANIA = TERCERO.COMPANIA
AND SOL.TERCERO = TERCERO.NIT
AND SOL.SUCURSAL = TERCERO.SUCURSAL
LEFT JOIN TERCERO TER
ON SOL.COMPANIA = TER.COMPANIA
AND SOL.BENEFICIARIO = TER.NIT
AND SOL.SUCURSAL = TER.SUCURSAL
LEFT JOIN TERCERO TER2
ON SOL.COMPANIA = TER2.COMPANIA
AND SOL.RESPONSABLE_APROBAR = TER2.NIT
AND SOL.SUCURSAL = TER2.SUCURSAL
LEFT JOIN REFERENCIA
ON SOL.COMPANIA = REFERENCIA.COMPANIA
AND SOL.ANO = REFERENCIA.ANO
AND SOL.REFERENCIA = REFERENCIA.CODIGO
WHERE SOL.COMPANIA = 's$compania$s'
AND SOL.ANO = s$ano$s
AND SOL.NUMERO LIKE 's$numeroSolicitud$s'
AND SOL.TIPO_SOLICITUD LIKE 's$tipoSolicitud$s']') CONSULTA, 3 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);