SELECT
    RUBRO,
    PLAN_PRESUPUESTAL_NOMBRE NOMBRE,
    TIPO_CPTE TIPO,
    TO_CHAR(COMPROBANTE) COMPROBANTE,
    TO_CHAR(FECHA,'DD/MM/YYYY') FECHA,
    CODTERCERO TERCERO,
    CODSUCURSAL SUCURSAL,
    NOMBRETERCERO NOMBRE_TERCERO,
    CONTRATO,
    DESCRIPCION,
    MOVIMIENTO,
    AFECTACION,
    MODIFICACION,
    SALDO
FROM
    (s$consultaBase$s)
ORDER BY TIPO_CPTE, RUBRO, COMPROBANTE, FECHA
