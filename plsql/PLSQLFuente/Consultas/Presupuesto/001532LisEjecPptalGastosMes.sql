WITH LISEJECPPTALGASTOSAC AS (
  SELECT SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,
  SALDO_PLAN_PPTAL.CODIGO,
  PLAN_PRESUPUESTAL.NOMBRE,
  SUM(APROPIACION_DEBITO-APROPIACION_CREDITO) APROPIADO,
  SUM(SALDO_PLAN_PPTAL.ADICION) ADICION,
  SUM(SALDO_PLAN_PPTAL.REDUCCION) REDUCCION,
  SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO) CREDITO,
  SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO) CONTRACREDITO,
  SUM(APLAZAM_DEBITO-APLAZAM_CREDITO) APLAZAMIENTO,
  SUM(APROPIACION_DEBITO-APROPIACION_CREDITO)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO)+SUM(SALDO_PLAN_PPTAL.REDUCCION)-SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO) +SUM(APLAZAM_DEBITO-APLAZAM_CREDITO) TOTALAPROPIADO,
  SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD) DISPONIBILIDAD,
  SUM(SALDO_PLAN_PPTAL.REG_CONTRACT) REGCONT,
  SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT) REGNOCONT,
  SUM(SALDO_PLAN_PPTAL.REG_REVERSION) REGREVERSION,
  SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT) MODREGCONT,
  SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT) MODREGNOCONT,
  SUM(REGISTRO_OBLIGACION+MODIF_REGISTRO_OBLIGACION) REGISTROOBLIGACION,
  SUM(SALDO_PLAN_PPTAL.REG_CONTRACT) + SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT) +SUM(SALDO_PLAN_PPTAL.REG_REVERSION)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT) APRREGISTRADAS,
  (SUM(APROPIACION_DEBITO - APROPIACION_CREDITO)+SUM(SALDO_PLAN_PPTAL.ADICION) +SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO)+SUM(SALDO_PLAN_PPTAL.REDUCCION)-SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO) +SUM(APLAZAM_DEBITO-APLAZAM_CREDITO)) -SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD) SALDODISPONIBLE,
  (SUM(APROPIACION_DEBITO - APROPIACION_CREDITO)+SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO)+SUM(SALDO_PLAN_PPTAL.REDUCCION)- SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO)+SUM(APLAZAM_DEBITO-APLAZAM_CREDITO))-(SUM(SALDO_PLAN_PPTAL.REG_CONTRACT) +SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)+SUM(SALDO_PLAN_PPTAL.REG_REVERSION)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT)) SALDOPORCOMP,
  SUM(EJE_PPT_DEBITO - EJE_PPT_CREDITO) EJEPTO,
  (SUM(APROPIACION_DEBITO - APROPIACION_CREDITO)+SUM(SALDO_PLAN_PPTAL.ADICION) +SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO) +SUM(SALDO_PLAN_PPTAL.REDUCCION)-SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO)+SUM(APLAZAM_DEBITO-APLAZAM_CREDITO)) -SUM(EJE_PPT_DEBITO -EJE_PPT_CREDITO) SALDOPOREJECUTAR,
  SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD)-(SUM(SALDO_PLAN_PPTAL.REG_CONTRACT) +SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)+SUM(SALDO_PLAN_PPTAL.REG_REVERSION)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT)) DISPABIERTASANTERIORES
FROM PLAN_PRESUPUESTAL
INNER JOIN SALDO_PLAN_PPTAL
ON PLAN_PRESUPUESTAL.CODIGO    = SALDO_PLAN_PPTAL.CODIGO
AND PLAN_PRESUPUESTAL.ANO      = SALDO_PLAN_PPTAL.ANO
AND PLAN_PRESUPUESTAL.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
WHERE SALDO_PLAN_PPTAL.COMPANIA= 's$compania$s'
AND SALDO_PLAN_PPTAL.ANO       = s$anio$s
AND SALDO_PLAN_PPTAL.MES BETWEEN 0 AND s$mes$s-1
AND SALDO_PLAN_PPTAL.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
AND PLAN_PRESUPUESTAL.NATURALEZA = 'D'
GROUP BY SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,
  SALDO_PLAN_PPTAL.CODIGO,
  PLAN_PRESUPUESTAL.NOMBRE
)
SELECT 
  SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)+SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)+SUM(SALDO_PLAN_PPTAL.REG_REVERSION)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT) APRREGISTRADAS,
  PLAN_PRESUPUESTAL.MOVIMIENTO,
  PLAN_PRESUPUESTAL.NOMBRE,
  SALDO_PLAN_PPTAL.COMPANIA,
  SUM(TRASLADO_CREDITO+APLAZAM_CREDITO) CONTRACREDITO,
  SUM(TRASLADO_DEBITO +APLAZAM_DEBITO)  CREDITO,
  SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD)  DISPONIBILIDAD,
  SUM(EJE_PPT_DEBITO -EJE_PPT_CREDITO)   EJEPTO,
  SUM(SALDO_PLAN_PPTAL.ADICION)     ADICION,
  SUM(SALDO_PLAN_PPTAL.REDUCCION)   REDUCCION,
  SUM(REGISTRO_OBLIGACION+MODIF_REGISTRO_OBLIGACION) REGISTROOBLIGACION,
  CASE WHEN s$tipoCuenta$s=1 THEN 
  SALDO_PLAN_PPTAL.CODIGO ELSE NVL(PLAN_PRESUPUESTAL.NIVEL1,'')
  || '-'
  || NVL(PLAN_PRESUPUESTAL.NIVEL2,'')
  || '-'
  || NVL(PLAN_PRESUPUESTAL.NIVEL3,'')
  || '-'
  || NVL(PLAN_PRESUPUESTAL.NIVEL4,'')
  || '-'
  || NVL(PLAN_PRESUPUESTAL.NIVEL5,'')
  || '-'
  || NVL(PLAN_PRESUPUESTAL.NIVEL6,'')
  || '-'
  || NVL(PLAN_PRESUPUESTAL.NIVEL7,'') END     RUBRO,
  LISEJECPPTALGASTOSAC.SALDODISPONIBLE +((SUM(SALDO_PLAN_PPTAL.ADICION)+SUM(TRASLADO_DEBITO +APLAZAM_DEBITO) +SUM(SALDO_PLAN_PPTAL.REDUCCION) )-(SUM(TRASLADO_CREDITO+APLAZAM_CREDITO)))-SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD)   SALDODISPON,
  LISEJECPPTALGASTOSAC.TOTALAPROPIADO     SALDOMES,
  CASE WHEN s$tipoCuenta$s=1 THEN 'CODIGO CUENTA' ELSE 'FID-UNE-PROG-SPR-PROY-DIS-REC' END  TITULOCUENTA,
  (LISEJECPPTALGASTOSAC.TOTALAPROPIADO+SUM(SALDO_PLAN_PPTAL.ADICION) +SUM(TRASLADO_DEBITO +APLAZAM_DEBITO)+SUM(SALDO_PLAN_PPTAL.REDUCCION))-(SUM(TRASLADO_CREDITO+APLAZAM_CREDITO))      TOTALAPROPIADO,
  LISEJECPPTALGASTOSAC.DISPONIBILIDAD +SUM(SALDO_PLAN_PPTAL.DISPONIBILIDAD) -(SUM(SALDO_PLAN_PPTAL.REG_CONTRACT)+SUM(SALDO_PLAN_PPTAL.REG_NO_CONTRACT)+SUM(SALDO_PLAN_PPTAL.REG_REVERSION)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_CONT)+SUM(SALDO_PLAN_PPTAL.MODIF_REG_NOCONT))-LISEJECPPTALGASTOSAC.APRREGISTRADAS   TOTALDISPONIBILIDAD
FROM PLAN_PRESUPUESTAL
INNER JOIN LISEJECPPTALGASTOSAC
INNER JOIN SALDO_PLAN_PPTAL
ON LISEJECPPTALGASTOSAC.CODIGO    = SALDO_PLAN_PPTAL.CODIGO
AND LISEJECPPTALGASTOSAC.ANO      = SALDO_PLAN_PPTAL.ANO
AND LISEJECPPTALGASTOSAC.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
ON PLAN_PRESUPUESTAL.CODIGO           = SALDO_PLAN_PPTAL.CODIGO
AND PLAN_PRESUPUESTAL.ANO         = SALDO_PLAN_PPTAL.ANO
AND PLAN_PRESUPUESTAL.COMPANIA    = SALDO_PLAN_PPTAL.COMPANIA
WHERE SALDO_PLAN_PPTAL.COMPANIA= 's$compania$s'
AND SALDO_PLAN_PPTAL.ANO = s$anio$s
AND SALDO_PLAN_PPTAL.MES     = s$mes$s
AND SALDO_PLAN_PPTAL.CODIGO BETWEEN  's$cuentaInicial$s' AND 's$cuentaFinal$s'
AND PLAN_PRESUPUESTAL.NATURALEZA = 'D'
AND LENGTH(SALDO_PLAN_PPTAL.CODIGO) BETWEEN 0 AND s$nivel$s
GROUP BY PLAN_PRESUPUESTAL.MOVIMIENTO,
  SALDO_PLAN_PPTAL.COMPANIA,
  SALDO_PLAN_PPTAL.ANO,
  SALDO_PLAN_PPTAL.CODIGO,
  PLAN_PRESUPUESTAL.NOMBRE,
  LISEJECPPTALGASTOSAC.TOTALAPROPIADO,
  LISEJECPPTALGASTOSAC.DISPONIBILIDAD,
  LISEJECPPTALGASTOSAC.APRREGISTRADAS,
  LISEJECPPTALGASTOSAC.SALDODISPONIBLE,
  PLAN_PRESUPUESTAL.NATURALEZA,
  SALDO_PLAN_PPTAL.CODIGO,
  LISEJECPPTALGASTOSAC.SALDOPOREJECUTAR,
  LISEJECPPTALGASTOSAC.SALDOPORCOMP,
  PLAN_PRESUPUESTAL.NATURALEZA,
  PLAN_PRESUPUESTAL.NIVEL1,
  PLAN_PRESUPUESTAL.NIVEL2,
  PLAN_PRESUPUESTAL.NIVEL3,
  PLAN_PRESUPUESTAL.NIVEL4,
  PLAN_PRESUPUESTAL.NIVEL5,
  PLAN_PRESUPUESTAL.NIVEL6,
  PLAN_PRESUPUESTAL.NIVEL7
