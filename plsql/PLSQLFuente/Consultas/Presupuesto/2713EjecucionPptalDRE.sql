        V_RESUMENPPTO_BASE.CODIGO  CUENTA,
        V_RESUMENPPTO_BASE.ID CODIGO,
        V_RESUMENPPTO_BASE.NOMBRE,
        V_RESUMENPPTO_BASE.ANO,
        SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE) APROPIACION,
        SUM(V_RESUMENPPTO_BASE.DISPONIBILIDAD) DISPONIBILIDAD,
        SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE - V_RESUMENPPTO_BASE.DISPONIBILIDAD )PORDISPONERDIS,
        SUM(V_RESUMENPPTO_BASE.COMPROMISOSACUM) REGISTRO,
        SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE - V_RESUMENPPTO_BASE.COMPROMISOSACUM )PORDISPONERRRES,
        SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT)EJECUTADO,
        SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE - V_RESUMENPPTO_BASE.EJECUCIONPPT)PORDISPONEREJE,
        V_RESUMENPPTO_BASE.FUENTE_RECURSO ||' - '|| V_RESUMENPPTO_BASE.NOMBREFUENTE AS FUENTE,
        V_RESUMENPPTO_BASE.FUENTECUIPO ||' - '|| TC.NOMBRE  FUENTECUIPO
FROM V_RESUMENPPTO_BASE
 LEFT JOIN TIPOCLASIFICADOR TC ON V_RESUMENPPTO_BASE.COMPANIA = TC.COMPANIA
         AND V_RESUMENPPTO_BASE.ANO = TC.ANO AND V_RESUMENPPTO_BASE.FUENTECUIPO = TC.CODIGO

WHERE V_RESUMENPPTO_BASE.COMPANIA = s$compania$s
  AND V_RESUMENPPTO_BASE.ANO = s$ano$s
  AND V_RESUMENPPTO_BASE.MES BETWEEN 0 AND s$mes$s
  AND V_RESUMENPPTO_BASE.ID BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  AND V_RESUMENPPTO_BASE.NATURALEZA IN('D')
   s$movimiento$s 
GROUP BY  V_RESUMENPPTO_BASE.CODIGO,
          V_RESUMENPPTO_BASE.ID ,
          V_RESUMENPPTO_BASE.NOMBRE,
          V_RESUMENPPTO_BASE.ANO,
          V_RESUMENPPTO_BASE.FUENTE_RECURSO ||' - '|| V_RESUMENPPTO_BASE.NOMBREFUENTE,
          V_RESUMENPPTO_BASE.FUENTECUIPO ||' - '|| TC.NOMBRE) 
SELECT  CODIGO,
        CUENTA,
        NOMBRE, 
        ANO,
        APROPIACION ,
        DISPONIBILIDAD,
        PORDISPONERDIS,
        REGISTRO ,
        PORDISPONERRRES, 
        EJECUTADO,
        PORDISPONEREJE,
        CASE WHEN APROPIACION NOT IN(0) 
          THEN PCK_SYSMAN_UTL.FC_ROUND(s$tipoEjecucion$s / APROPIACION * 100,2)
          ELSE 100
        END PORCENTAJE,
        FUENTE,
        FUENTECUIPO 
FROM EJECUCIONPPTAL]') CONSULTA, 3 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);
