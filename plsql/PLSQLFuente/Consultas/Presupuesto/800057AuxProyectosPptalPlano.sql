SELECT 
   CP.ANO||CP.COD_PROYECTO_PPTAL CODPROYECTO_PPTAL
  ,CP.COD_PROYECTO_PPTAL
  ,DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE
  ,DETALLE_COMPROBANTE_PPTAL.COMPROBANTE
  ,TO_CHAR(DETALLE_COMPROBANTE_PPTAL.FECHA,'DD/MM/YYYY') FECHA
  ,RPAD(TERCERO.NIT                          ,20) NIT
  ,TERCERO.NOMBRE NOMBRETERCERO
  ,DETALLE_COMPROBANTE_PPTAL.CUENTA CODIGO_CUENTA
  ,RPAD(PLAN_PRESUPUESTAL.NOMBRE             ,30) NOMBRERUBRO
  ,RPAD(DETALLE_COMPROBANTE_PPTAL.DESCRIPCION,30) DESCRIPCION
  ,RPAD(SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA IN('D')
                 THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO -DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)
                 ELSE  DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO-DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO
            END)
       ,10) VALOR_COMPROBANTE
  ,SUM(
     CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA IN('D')
          THEN (DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO -DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO)
          ELSE  DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO
     END
   ) VALOR_AFECTADO
  ,RPAD(SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA IN('D')
                 THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO     - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO   ) -
                      (DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO  - DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO)
                 ELSE (DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO    - DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO    ) -
                      (DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO - DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO )
                 END)
       ,10) SALDO
  ,CASE WHEN DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE IN('EGR')
        THEN BANCO.NOMBREBANCO
   END NOMBREBANCOO
  ,CASE WHEN DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE IN('EGR')
        THEN COMPROBANTE_CNT.NRO_DOCUMENTO
   END NRO_CHEQUE
  ,ASIGNACION_RECURSOS.NOMBRE NOMASIGNACION
  ,FUENTE_FINANCIACION.NOMBRE
  ,COMPROBANTE_PPTALAFECTADOS.TIPO_CPTE_AFECT
  ,COMPROBANTE_PPTALAFECTADOS.COMPROBANTE_AFECT CMPTE_AFECTADO
  ,CASE WHEN DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT IS NOT NULL
        THEN DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT || ' - '
   END ||
   CASE WHEN NVL(DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO,0) NOT IN(0)
        THEN TO_CHAR(DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO)
   END DOCUMENTOAFECTADO
  ,DETALLE_COMPROBANTE_PPTAL.TERCERO
  ,RPAD(PROYECTOS_PPTAL.VLR_PROYECTO,10) VLR_PROYECTO
  ,PROYECTOS_PPTAL.NOMBRE NOMBRE_PROYECTO
FROM DETALLE_COMPROBANTE_PPTAL
  INNER JOIN COMPROBANTE_PPTAL CP
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA    = CP.COMPANIA 
    AND DETALLE_COMPROBANTE_PPTAL.ANO         = CP.ANO
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = CP.TIPO
    AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = CP.NUMERO
  INNER JOIN PLAN_PRESUPUESTAL
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO      = PLAN_PRESUPUESTAL.ANO
    AND DETALLE_COMPROBANTE_PPTAL.CUENTA   = PLAN_PRESUPUESTAL.CODIGO
  INNER JOIN TIPO_COMPROBPP
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA  = TIPO_COMPROBPP.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
  INNER JOIN TERCERO
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = TERCERO.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.TERCERO  = TERCERO.NIT
    AND DETALLE_COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL
  LEFT JOIN ASIGNACION_RECURSOS
     ON CP.COMPANIA   = ASIGNACION_RECURSOS.COMPANIA
    AND CP.ASIGNACION = ASIGNACION_RECURSOS.CODIGO
  LEFT JOIN TERCEROPAGOS
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = TERCEROPAGOS.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.TERCERO  = TERCEROPAGOS.NIT
    AND DETALLE_COMPROBANTE_PPTAL.SUCURSAL = TERCEROPAGOS.SUCURSAL
  LEFT JOIN BANCO
     ON TERCEROPAGOS.COMPANIA = BANCO.COMPANIA
    AND TERCEROPAGOS.BANCO    = BANCO.BANCO
  LEFT JOIN FUENTE_FINANCIACION
     ON CP.COMPANIA            = FUENTE_FINANCIACION.COMPANIA
    AND CP.ANO                 = FUENTE_FINANCIACION.ANO
    AND CP.FUENTE_FINANCIACION = FUENTE_FINANCIACION.CODIGO
  LEFT JOIN COMPROBANTE_CNT
     ON  DETALLE_COMPROBANTE_PPTAL.COMPANIA   = COMPROBANTE_CNT.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO         = COMPROBANTE_CNT.ANO
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = COMPROBANTE_CNT.TIPO
    AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_CNT.NUMERO
  INNER JOIN COMPROBANTE_PPTALAFECTADOS
     ON  CP.COMPANIA = COMPROBANTE_PPTALAFECTADOS.COMPANIA
    AND CP.ANO       = COMPROBANTE_PPTALAFECTADOS.ANO
    AND CP.TIPO      = COMPROBANTE_PPTALAFECTADOS.TIPO_CPTE 
    AND CP.NUMERO    = COMPROBANTE_PPTALAFECTADOS.COMPROBANTE
  LEFT JOIN PROYECTOS_PPTAL
     ON  CP.COMPANIA          = PROYECTOS_PPTAL.COMPANIA
    AND CP.ANO                = PROYECTOS_PPTAL.ANO
    AND CP.COD_PROYECTO_PPTAL = PROYECTOS_PPTAL.CODIGO
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA  = s$compania$s
  AND DETALLE_COMPROBANTE_PPTAL.FECHA      BETWEEN  s$fechaInicial$s     AND  s$fechaFinal$s
  AND DETALLE_COMPROBANTE_PPTAL.CUENTA     BETWEEN 's$cuentaInicial$s'   AND 's$cuentaFinal$s'
  AND DETALLE_COMPROBANTE_PPTAL.TERCERO    BETWEEN 's$terceroInicial$s'  AND 's$terceroFinal$s'
  AND CP.COD_PROYECTO_PPTAL                BETWEEN 's$proyectoInicial$s' AND 's$proyectoFinal$s'
  AND CP.TIPO IN (s$tipos$s)
GROUP BY 
   CP.ANO || CP.COD_PROYECTO_PPTAL
  ,CP.COD_PROYECTO_PPTAL
  ,DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE
  ,DETALLE_COMPROBANTE_PPTAL.COMPROBANTE
  ,DETALLE_COMPROBANTE_PPTAL.FECHA
  ,TERCERO.NIT
  ,TERCERO.NOMBRE
  ,DETALLE_COMPROBANTE_PPTAL.CUENTA
  ,PLAN_PRESUPUESTAL.NOMBRE
  ,DETALLE_COMPROBANTE_PPTAL.DESCRIPCION
  ,CASE WHEN DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE IN('EGR')
        THEN BANCO.NOMBREBANCO
   END
  ,CASE WHEN DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE IN('EGR')
        THEN COMPROBANTE_CNT.NRO_DOCUMENTO
   END
  ,ASIGNACION_RECURSOS.NOMBRE
  ,FUENTE_FINANCIACION.NOMBRE
  ,COMPROBANTE_PPTALAFECTADOS.TIPO_CPTE_AFECT
  ,COMPROBANTE_PPTALAFECTADOS.COMPROBANTE_AFECT
  ,CASE WHEN DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT IS NOT NULL
        THEN DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT || ' - '
   END ||
   CASE WHEN NVL(DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO,0) NOT IN(0)
        THEN TO_CHAR(DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO)
   END
  ,DETALLE_COMPROBANTE_PPTAL.TERCERO
  ,PROYECTOS_PPTAL.VLR_PROYECTO
  ,PROYECTOS_PPTAL.NOMBRE
ORDER BY DETALLE_COMPROBANTE_PPTAL.CUENTA
