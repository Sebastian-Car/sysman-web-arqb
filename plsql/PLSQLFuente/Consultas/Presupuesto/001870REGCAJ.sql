SELECT 
  V_DETALLE_AUXILIAR_PPTAL.TIPO_COMPROBANTE_NOM AS NOMBRET,
  V_DETALLE_AUXILIAR_PPTAL.COMPROBANTE,
  TO_CHAR(V_DETALLE_AUXILIAR_PPTAL.FECHA,'DD/MM/YYYY') FECHA,
  V_DETALLE_AUXILIAR_PPTAL.TERCERO_COM_NOM AS NOMTERCEROCOM,
  V_DETALLE_AUXILIAR_PPTAL.TERCERO_COM AS NITTERCCOMP,
  V_DETALLE_AUXILIAR_PPTAL.CUENTA AS CODIGO_CUENTA,
  V_DETALLE_AUXILIAR_PPTAL.CUENTANOMBRE NOMBRE,
  V_DETALLE_AUXILIAR_PPTAL.CENTRO_COSTO_DET AS CENTRO_COSTO,
  CASE WHEN  V_DETALLE_AUXILIAR_PPTAL.AUXILIAR_DET IN ('99999999999999999999') THEN '' ELSE AUXILIAR_DET END CODAUXILIAR,
  CASE WHEN  V_DETALLE_AUXILIAR_PPTAL.FUENTE_RECURSOS IN ('99999999999999999999') THEN '' ELSE FUENTE_RECURSOS END CODFUENTE,
  V_DETALLE_AUXILIAR_PPTAL.TIPO_CPTE_AFECT,
  V_DETALLE_AUXILIAR_PPTAL.CMPTE_AFECTADO,
  V_DETALLE_AUXILIAR_PPTAL.VALOR_DEBITO,
  V_DETALLE_AUXILIAR_PPTAL.VALOR_CREDITO,
  DETALLE_AFECTADO.VALOR_DEBITO-(DETALLE_AFECTADO.DEBITO_AFECTADO- DETALLE_AFECTADO.CREDITO_AFECTADO)+( DETALLE_AFECTADO.MODIFICACION_DEBITO-DETALLE_AFECTADO.MODIFICACION_CREDITO) AS VALORDISPONIBLE,
  V_DETALLE_AUXILIAR_PPTAL.DESCRIPCION,
  NVL(V_DETALLE_AUXILIAR_PPTAL.TEXTO,' ') AS TXT,
  V_DETALLE_AUXILIAR_PPTAL.CREATED_BY AS CREADORCOM,
  TO_CHAR(V_DETALLE_AUXILIAR_PPTAL.FECHA_COM,'DD') || ' ' || 'dia(s) del mes de ' || 
  UPPER(PCK_SYSMAN_UTL.FC_NOMBRE_MES(TO_CHAR(V_DETALLE_AUXILIAR_PPTAL.FECHA_COM,'MM'))) || ' de ' || TO_CHAR(V_DETALLE_AUXILIAR_PPTAL.FECHA_COM,'YYYY') FECHALETRAS
FROM V_DETALLE_AUXILIAR_PPTAL
LEFT JOIN DETALLE_COMPROBANTE_PPTAL DETALLE_AFECTADO
     ON  V_DETALLE_AUXILIAR_PPTAL.COMPANIA          = DETALLE_AFECTADO.COMPANIA 
    AND V_DETALLE_AUXILIAR_PPTAL.ANO_AFECT          = DETALLE_AFECTADO.ANO
    AND V_DETALLE_AUXILIAR_PPTAL.TIPO_CPTE_AFECT    = DETALLE_AFECTADO.TIPO_CPTE
    AND V_DETALLE_AUXILIAR_PPTAL.CMPTE_AFECTADO     = DETALLE_AFECTADO.COMPROBANTE
    AND V_DETALLE_AUXILIAR_PPTAL.CONSECUTIVOPPTO    = DETALLE_AFECTADO.CONSECUTIVO
    AND V_DETALLE_AUXILIAR_PPTAL.CUENTA             = DETALLE_AFECTADO.CUENTA
    AND V_DETALLE_AUXILIAR_PPTAL.FUENTE_RECURSOS  = DETALLE_AFECTADO.FUENTE_RECURSO
    AND V_DETALLE_AUXILIAR_PPTAL.AUXILIAR        = DETALLE_AFECTADO.AUXILIAR
    AND V_DETALLE_AUXILIAR_PPTAL.REFERENCIA      = DETALLE_AFECTADO.REFERENCIA
    AND V_DETALLE_AUXILIAR_PPTAL.TERCERO         = DETALLE_AFECTADO.TERCERO 
    AND V_DETALLE_AUXILIAR_PPTAL.SUCURSAL        = DETALLE_AFECTADO.SUCURSAL
WHERE V_DETALLE_AUXILIAR_PPTAL.COMPANIA          = s$compania$s
  AND V_DETALLE_AUXILIAR_PPTAL.ANO                 = s$ano$s
  AND V_DETALLE_AUXILIAR_PPTAL.TIPO                =  's$tipoCpte$s'
  AND V_DETALLE_AUXILIAR_PPTAL.COMPROBANTE BETWEEN s$numeroPptoInicial$s AND s$numeroPptoFinal$s
