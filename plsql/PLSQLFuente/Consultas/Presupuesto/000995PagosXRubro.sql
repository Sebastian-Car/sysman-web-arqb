SELECT COMPROBANTE_PPTAL.FECHA, 
       COMPROBANTE_PPTAL.TERCERO, 
       TERCERO.NOMBRE, 
       COMPROBANTE_PPTAL.VLR_DOCUMENTO,
       SUM(CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA = 'D'
                THEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO
                ELSE DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO - DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO
           END) VALORTOTAL
FROM DETALLE_COMPROBANTE_PPTAL
      INNER JOIN COMPROBANTE_PPTAL 
          INNER JOIN TERCERO 
                 ON COMPROBANTE_PPTAL.COMPANIA = TERCERO.COMPANIA  
                AND COMPROBANTE_PPTAL.TERCERO  = TERCERO.NIT  
		AND COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL 
             ON DETALLE_COMPROBANTE_PPTAL.COMPANIA    = COMPROBANTE_PPTAL.COMPANIA 
            AND DETALLE_COMPROBANTE_PPTAL.ANO         = COMPROBANTE_PPTAL.ANO 
            AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = COMPROBANTE_PPTAL.TIPO         
            AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO 
    INNER JOIN TIPO_COMPROBPP 
           ON COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA  
          AND COMPROBANTE_PPTAL.TIPO     = TIPO_COMPROBPP.CODIGO  
    INNER JOIN PLAN_PRESUPUESTAL
           ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA  
          AND DETALLE_COMPROBANTE_PPTAL.ANO      = PLAN_PRESUPUESTAL.ANO 
          AND DETALLE_COMPROBANTE_PPTAL.CUENTA   = PLAN_PRESUPUESTAL.CODIGO
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA = s$compania$s 
  AND DETALLE_COMPROBANTE_PPTAL.ANO      = s$anio$s 
  AND DETALLE_COMPROBANTE_PPTAL.CUENTA   = 's$cuentaInicial$s' 
  AND COMPROBANTE_PPTAL.FECHA BETWEEN s$fechaInicial$s AND s$fechaFinal$s 
  AND TIPO_COMPROBPP.CLASE               = 'EGR' 
GROUP BY COMPROBANTE_PPTAL.FECHA, 
         COMPROBANTE_PPTAL.TERCERO,  
         TERCERO.NOMBRE,  
         COMPROBANTE_PPTAL.VLR_DOCUMENTO, 
         COMPROBANTE_PPTAL.FECHA, 
         DETALLE_COMPROBANTE_PPTAL.CUENTA, 
         DETALLE_COMPROBANTE_PPTAL.COMPANIA, 
         DETALLE_COMPROBANTE_PPTAL.ANO, 
         TIPO_COMPROBPP.CLASE  
