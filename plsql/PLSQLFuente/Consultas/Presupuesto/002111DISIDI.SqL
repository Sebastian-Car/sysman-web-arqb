SELECT
    COMPROBANTE_PPTAL.FECHA AS FECHA_CPTE,
    COMPROBANTE_PPTAL.CARGO AS CARGO,
    COMPROBANTE_PPTAL.DESCRIPCION || ' - ' || COMPROBANTE_PPTAL.TEXTO || ' - ' || COMPROBANTE_PPTAL.NRO_DOCUMENTO AS OBJETO,
    TO_CHAR(COMPROBANTE_PPTAL.FECHA, 'DD') AS DIA,
    TO_CHAR(COMPROBANTE_PPTAL.FECHA, 'MONTH') AS MES,
    TO_CHAR(COMPROBANTE_PPTAL.FECHA, 'YYYY') AS ANIO,
    DETALLE_COMPROBANTE_PPTAL.COMPROBANTE AS NUMERO_CPTE,
    DETALLE_COMPROBANTE_PPTAL.ANO AS PARANO,
    DETALLE_COMPROBANTE_PPTAL.CUENTA AS RUBRO,
    SUM(DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO) AS VALOR_DEBITO,
    PLAN_PRESUPUESTAL.NOMBRE AS NOMBRE_PPTAL,
    TERCERO.NOMBRE AS NOMBRE_TERCERO,
    DEPENDENCIA.NOMBRE AS DEPENDENCIA,
    UPPER(TIPO_COMPROBPP.NOMBRE) AS NOMBRE_TIPO_CPTE
FROM 
    COMPROBANTE_PPTAL
    INNER JOIN DETALLE_COMPROBANTE_PPTAL 
        ON COMPROBANTE_PPTAL.NUMERO = DETALLE_COMPROBANTE_PPTAL.COMPROBANTE 
        AND COMPROBANTE_PPTAL.TIPO = DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE 
        AND COMPROBANTE_PPTAL.ANO = DETALLE_COMPROBANTE_PPTAL.ANO 
        AND COMPROBANTE_PPTAL.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA
    INNER JOIN TERCERO 
        ON TERCERO.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA 
        AND TERCERO.SUCURSAL = DETALLE_COMPROBANTE_PPTAL.SUCURSAL 
        AND TERCERO.NIT = DETALLE_COMPROBANTE_PPTAL.TERCERO 
    LEFT JOIN TIPO_COMPROBPP 
        ON COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA 
        AND COMPROBANTE_PPTAL.TIPO = TIPO_COMPROBPP.CODIGO
    LEFT JOIN DEPENDENCIA 
        ON COMPROBANTE_PPTAL.COMPANIA = DEPENDENCIA.COMPANIA 
        AND COMPROBANTE_PPTAL.DEPENDENCIA = DEPENDENCIA.CODIGO     
    LEFT JOIN PLAN_PRESUPUESTAL 
        ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA 
        AND DETALLE_COMPROBANTE_PPTAL.ANO = PLAN_PRESUPUESTAL.ANO 
        AND DETALLE_COMPROBANTE_PPTAL.CUENTA = PLAN_PRESUPUESTAL.CODIGO
WHERE 
    COMPROBANTE_PPTAL.COMPANIA = s$compania$s
    AND COMPROBANTE_PPTAL.ANO = s$ano$s
    AND COMPROBANTE_PPTAL.TIPO = 's$tipoCpte$s'
    AND COMPROBANTE_PPTAL.NUMERO BETWEEN s$numeroPptoInicial$s AND s$numeroPptoFinal$s
GROUP BY 
    COMPROBANTE_PPTAL.FECHA,
    COMPROBANTE_PPTAL.DESCRIPCION || ' - ' || COMPROBANTE_PPTAL.TEXTO || ' - ' || COMPROBANTE_PPTAL.NRO_DOCUMENTO,
    COMPROBANTE_PPTAL.CARGO,
    COMPROBANTE_PPTAL.FECHA,
    DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
    DETALLE_COMPROBANTE_PPTAL.ANO,
    DETALLE_COMPROBANTE_PPTAL.CUENTA,
    PLAN_PRESUPUESTAL.NOMBRE,
    TERCERO.NOMBRE,
    DEPENDENCIA.NOMBRE,
    TIPO_COMPROBPP.NOMBRE