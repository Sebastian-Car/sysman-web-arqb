            AND D_SOLICITUDDISPONIBILIDAD.RUBRO             = PLAN_PRESUPUESTAL.CODIGO   
        INNER JOIN  FUENTE_RECURSOS
            ON D_SOLICITUDDISPONIBILIDAD.COMPANIA         = FUENTE_RECURSOS.COMPANIA 
            AND D_SOLICITUDDISPONIBILIDAD.ANO         = FUENTE_RECURSOS.ANO
         AND D_SOLICITUDDISPONIBILIDAD.FUENTE        = FUENTE_RECURSOS.CODIGO
