SELECT 
	COMPROBANTE_PPTAL.FECHA AS FECHA_CPTE, 
	COMPROBANTE_PPTAL.TEXTO AS TEXTO_OBJETO, 
    TO_CHAR(COMPROBANTE_PPTAL.FECHA, 'DD') AS DIA,
    TO_CHAR(COMPROBANTE_PPTAL.FECHA, 'MONTH') AS MES,
    TO_CHAR(COMPROBANTE_PPTAL.FECHA, 'YYYY') AS ANIO,
	COMPROBANTE_PPTAL.DESCRIPCION || ' - ' || COMPROBANTE_PPTAL.NRO_DOCUMENTO AS TIPO_NUM_COMP,
	DETALLE_COMPROBANTE_PPTAL.COMPROBANTE AS NUMERO_CPTE, 
	DETALLE_COMPROBANTE_PPTAL_1.COMPROBANTE AS CPTE_AFECTADO,
	DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO AS VALOR_DEBITO,
	DETALLE_COMPROBANTE_PPTAL.CUENTA AS CODIGO_CUENTA,
	PLAN_PRESUPUESTAL.NOMBRE AS NOMBRE_PLAN_PPTAL,
	UPPER(TIPO_COMPROBPP.NOMBRE) AS NOMBRE_TIPO_CPTE,
	TERCERO.NIT AS CEDULA_TERCERO, 
	TERCERO.NOMBRE AS NOMBRE_TERCERO
FROM 
	COMPROBANTE_PPTAL  
	INNER JOIN DETALLE_COMPROBANTE_PPTAL 
		ON COMPROBANTE_PPTAL.TIPO = DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE
        AND COMPROBANTE_PPTAL.NUMERO = DETALLE_COMPROBANTE_PPTAL.COMPROBANTE 
        AND COMPROBANTE_PPTAL.ANO = DETALLE_COMPROBANTE_PPTAL.ANO 
        AND COMPROBANTE_PPTAL.COMPANIA = DETALLE_COMPROBANTE_PPTAL.COMPANIA
	INNER JOIN TERCERO
		ON COMPROBANTE_PPTAL.COMPANIA = TERCERO.COMPANIA 
		AND COMPROBANTE_PPTAL.TERCERO = TERCERO.NIT 
		AND COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL
	INNER JOIN TIPO_COMPROBPP 
		ON COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA 
		AND COMPROBANTE_PPTAL.TIPO = TIPO_COMPROBPP.CODIGO 
	LEFT JOIN PLAN_PRESUPUESTAL 
		ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA 
		AND DETALLE_COMPROBANTE_PPTAL.ANO = PLAN_PRESUPUESTAL.ANO 
		AND DETALLE_COMPROBANTE_PPTAL.CUENTA = PLAN_PRESUPUESTAL.CODIGO
	LEFT JOIN DETALLE_COMPROBANTE_PPTAL DETALLE_COMPROBANTE_PPTAL_1 
		ON DETALLE_COMPROBANTE_PPTAL.CONSECUTIVOPPTO = DETALLE_COMPROBANTE_PPTAL_1.CONSECUTIVO 
		AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT = DETALLE_COMPROBANTE_PPTAL_1.TIPO_CPTE 
		AND DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO = DETALLE_COMPROBANTE_PPTAL_1.COMPROBANTE 
		AND DETALLE_COMPROBANTE_PPTAL.COMPANIA = DETALLE_COMPROBANTE_PPTAL_1.COMPANIA 		
		AND DETALLE_COMPROBANTE_PPTAL.ANO = DETALLE_COMPROBANTE_PPTAL_1.ANO 
		AND DETALLE_COMPROBANTE_PPTAL.CUENTA = DETALLE_COMPROBANTE_PPTAL_1.CUENTA
WHERE 
	DETALLE_COMPROBANTE_PPTAL.COMPROBANTE IS NOT NULL 
	AND COMPROBANTE_PPTAL.COMPANIA = s$compania$s
    AND COMPROBANTE_PPTAL.ANO = s$ano$s    
    AND COMPROBANTE_PPTAL.TIPO = 's$tipoCpte$s'    
    AND COMPROBANTE_PPTAL.NUMERO BETWEEN s$numeroPptoInicial$s AND s$numeroPptoFinal$s