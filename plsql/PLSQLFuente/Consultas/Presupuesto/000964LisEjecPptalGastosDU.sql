SELECT  SALDO_PLAN_PPTAL.COMPANIA,
        SALDO_PLAN_PPTAL.ANO,
        PLAN_PRESUPUESTAL.ID,
        PLAN_PRESUPUESTAL.NOMBRE,
        SALDOANTERIOR.APROPIACIONINICIAL  APROPIACIONINICIAL,
        SUM(SALDO_PLAN_PPTAL.ADICION + SALDO_PLAN_PPTAL.REDUCCION + (TRASLADO_DEBITO - TRASLADO_CREDITO) + (APLAZAM_DEBITO - APLAZAM_CREDITO)) MODIFICACIONES,   
        SUM(REG_NO_CONTRACT + MODIF_REG_NOCONT) COMPROMISOSPRESUPUESTALES,
        SUM((EJE_PPT_DEBITO- EJE_PPT_CREDITO) + REINTEGRO) GIROSTESORERIA,
        SUM((SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION + SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION) - ((EJE_PPT_DEBITO- EJE_PPT_CREDITO) + REINTEGRO))  CXP,
        SUM((EJE_PPT_DEBITO- EJE_PPT_CREDITO) + REINTEGRO)  TOTALEJECUTADO
FROM V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
  LEFT JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
    ON SALDO_PLAN_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
    AND SALDO_PLAN_PPTAL.ANO     = PLAN_PRESUPUESTAL.ANO
    AND SALDO_PLAN_PPTAL.ID      = PLAN_PRESUPUESTAL.ID
  LEFT JOIN (SELECT  SALDO_PLAN_PPTAL.COMPANIA,
                      SALDO_PLAN_PPTAL.ANO,
                      PLAN_PRESUPUESTAL.ID,
                      SUM((APROPIACION_DEBITO- APROPIACION_CREDITO)+
                      (SALDO_PLAN_PPTAL.ADICION) +
                      (SALDO_PLAN_PPTAL.REDUCCION) + 
                      (APLAZAM_DEBITO - APLAZAM_CREDITO) +
                      (TRASLADO_DEBITO  - TRASLADO_CREDITO))APROPIACIONINICIAL
              FROM V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
                LEFT JOIN V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
                  ON SALDO_PLAN_PPTAL.COMPANIA   = PLAN_PRESUPUESTAL.COMPANIA
                  AND SALDO_PLAN_PPTAL.ANO       = PLAN_PRESUPUESTAL.ANO
                  AND SALDO_PLAN_PPTAL.ID        = PLAN_PRESUPUESTAL.ID
              WHERE SALDO_PLAN_PPTAL.COMPANIA = s$compania$s
                AND SALDO_PLAN_PPTAL.ANO    = s$ano$s
                AND PLAN_PRESUPUESTAL.NATURALEZA = 'D'
                AND SALDO_PLAN_PPTAL.MES        < s$mesInicial$s
                AND LENGTH(SALDO_PLAN_PPTAL.ID)     <= s$nivel$s
                AND PLAN_PRESUPUESTAL.CODIGO BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
              GROUP BY  SALDO_PLAN_PPTAL.COMPANIA,
                        SALDO_PLAN_PPTAL.ANO,
                        PLAN_PRESUPUESTAL.ID)
  SALDOANTERIOR  
    ON SALDO_PLAN_PPTAL.COMPANIA = SALDOANTERIOR.COMPANIA
    AND SALDO_PLAN_PPTAL.ANO     = SALDOANTERIOR.ANO
    AND SALDO_PLAN_PPTAL.ID      = SALDOANTERIOR.ID          
WHERE  SALDO_PLAN_PPTAL.COMPANIA = s$compania$s
	AND SALDO_PLAN_PPTAL.ANO    = s$ano$s
	AND SALDO_PLAN_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s
	AND PLAN_PRESUPUESTAL.NATURALEZA = 'D'
	AND PLAN_PRESUPUESTAL.CODIGO BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
	AND LENGTH(SALDO_PLAN_PPTAL.ID) <= s$nivel$s
GROUP BY SALDO_PLAN_PPTAL.COMPANIA,
        SALDO_PLAN_PPTAL.ANO,
        PLAN_PRESUPUESTAL.ID,
        PLAN_PRESUPUESTAL.NOMBRE,
       SALDOANTERIOR.APROPIACIONINICIAL
