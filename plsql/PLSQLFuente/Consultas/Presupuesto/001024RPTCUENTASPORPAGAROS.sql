SELECT SUBSTR(V_RESUMENPPTO_BASE.CODIGO,1,1) AS CLASE,
    V_RESUMENPPTO_BASE.CODIGO CUENTA,
    V_RESUMENPPTO_BASE.NOMBRE,
    CASE WHEN s$miles$s = 1 
         THEN ROUND(SUM(V_RESUMENPPTO_BASE.APROPIADO),-3)/1000
         ELSE SUM(V_RESUMENPPTO_BASE.APROPIADO)
    END APROPIACIONVIGENTE, 
    CASE WHEN s$miles$s = 1 
         THEN ROUND(SUM(V_RESUMENPPTO_BASE.ADICION  + V_RESUMENPPTO_BASE.TRASLADO + V_RESUMENPPTO_BASE.REDUCCION),-3)/1000
         ELSE  SUM(V_RESUMENPPTO_BASE.ADICION + V_RESUMENPPTO_BASE.TRASLADO + V_RESUMENPPTO_BASE.REDUCCION)
    END  ANULACIONES,		
    CASE WHEN s$miles$s = 1 
         THEN ROUND(SUM( V_RESUMENPPTO_BASE.APROPIADO + V_RESUMENPPTO_BASE.ADICION + V_RESUMENPPTO_BASE.TRASLADO + V_RESUMENPPTO_BASE.REDUCCION),-3)/1000
          ELSE  SUM( V_RESUMENPPTO_BASE.APROPIADO + V_RESUMENPPTO_BASE.ADICION + V_RESUMENPPTO_BASE.TRASLADO+ V_RESUMENPPTO_BASE.REDUCCION)
    END	AS APROPIACIONDEF,
    CASE WHEN s$miles$s = 1 
	     THEN ROUND(SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT),-3)/1000
         ELSE SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT)
    END PAGOSACUM, 
    SUM(CASE WHEN V_RESUMENPPTO_BASE.MES = s$mes$s
             THEN CASE WHEN s$miles$s = 1 
                  	   THEN ROUND(V_RESUMENPPTO_BASE.EJECUCIONPPT,-3)/1000
                  	   ELSE V_RESUMENPPTO_BASE.EJECUCIONPPT
                  END
        END) PAGOSMES,
    CASE WHEN s$miles$s = 1 
         THEN ROUND(
         		SUM(V_RESUMENPPTO_BASE.APROPIADO + V_RESUMENPPTO_BASE.ADICION + V_RESUMENPPTO_BASE.TRASLADO+ V_RESUMENPPTO_BASE.REDUCCION),
         		-3
         	  )/1000
         ELSE SUM(V_RESUMENPPTO_BASE.APROPIADO + V_RESUMENPPTO_BASE.ADICION + V_RESUMENPPTO_BASE.TRASLADO + V_RESUMENPPTO_BASE.REDUCCION)
    END -   
    CASE WHEN s$miles$s = 1 
         THEN ROUND(SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT),-3)/1000
         ELSE SUM(V_RESUMENPPTO_BASE.EJECUCIONPPT)
    END TOTALCXP
FROM V_RESUMENPPTO_BASE
WHERE V_RESUMENPPTO_BASE.COMPANIA = s$compania$s
  	AND V_RESUMENPPTO_BASE.ANO 	  = s$ano$s
  	AND V_RESUMENPPTO_BASE.ID BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
  	AND V_RESUMENPPTO_BASE.NATURALEZA = 'D'
  	AND V_RESUMENPPTO_BASE.MES 		 <= s$mes$s
  	AND LENGTH(V_RESUMENPPTO_BASE.CODIGO) BETWEEN 0 AND NVL(s$nivel$s,6)
  	AND V_RESUMENPPTO_BASE.INFORME NOT IN(0)
GROUP BY V_RESUMENPPTO_BASE.CODIGO,
	V_RESUMENPPTO_BASE.NOMBRE
ORDER BY V_RESUMENPPTO_BASE.CODIGO, V_RESUMENPPTO_BASE.NOMBRE
