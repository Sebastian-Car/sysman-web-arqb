SELECT  PLAN_PRESUPUESTAL.COMPANIA,
        SALDO_PLAN_PPTAL.ID ,
        PLAN_PRESUPUESTAL.CODIGO CUENTA,
        PLAN_PRESUPUESTAL.NOMBRE,
        SUM(APROPIACION_CREDITO- APROPIACION_DEBITO)  APROPIADO,
        SUM(ADICION)ADICION,
        SUM(TRASLADO_CREDITO  + APLAZAM_CREDITO ) CREDITOS,  
        SUM(TRASLADO_CREDITO  + APLAZAM_CREDITO + REDUCCION )CONTRACREDITOS,
        SUM(( APROPIACION_CREDITO - APROPIACION_DEBITO) + 
             ADICION + 
             REDUCCION +  
            (TRASLADO_CREDITO-TRASLADO_DEBITO) + 
            (APLAZAM_CREDITO-APLAZAM_DEBITO))TOTALPRESUPUESTO,
       SUM(CASE WHEN MES = s$mes$s THEN
                EJE_PPT_CREDITO-EJE_PPT_DEBITO+MODIF_INGRESOS_EFECTIVO+MODIF_INGRESOS_PAPELES
           ELSE 
           0
           END) AS RECAUDOSMES,
           SUM(CASE WHEN MES < s$mes$s THEN
                (EJE_PPT_CREDITO-EJE_PPT_DEBITO)+MODIF_INGRESOS_EFECTIVO+MODIF_INGRESOS_PAPELES
           ELSE 
           0
           END) AS RECAUDOSACUMULADOS,
           SUM((( APROPIACION_CREDITO - APROPIACION_DEBITO) + 
             ADICION + 
             REDUCCION +  
            (TRASLADO_CREDITO-TRASLADO_DEBITO) + 
            (APLAZAM_CREDITO-APLAZAM_DEBITO)) -
            ((EJE_PPT_CREDITO     - EJE_PPT_DEBITO) +MODIF_INGRESOS_EFECTIVO+MODIF_INGRESOS_PAPELES)) AS PORRECAUDAR
FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
    LEFT JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
        ON PLAN_PRESUPUESTAL.COMPANIA        = SALDO_PLAN_PPTAL.COMPANIA 
        AND PLAN_PRESUPUESTAL.ANO            = SALDO_PLAN_PPTAL.ANO
        AND PLAN_PRESUPUESTAL.ID             = SALDO_PLAN_PPTAL.ID
WHERE PLAN_PRESUPUESTAL.COMPANIA = s$compania$s
    AND PLAN_PRESUPUESTAL.ANO    = s$ano$s
    AND SALDO_PLAN_PPTAL.MES     BETWEEN 0 AND  s$mes$s
    AND PLAN_PRESUPUESTAL.ID BETWEEN 's$codigoInicial$s' AND 's$codigoFinal$s'
GROUP BY  PLAN_PRESUPUESTAL.COMPANIA,
          PLAN_PRESUPUESTAL.ANO,
          PLAN_PRESUPUESTAL.ID,
          SALDO_PLAN_PPTAL.ID,
          PLAN_PRESUPUESTAL.CODIGO,
          PLAN_PRESUPUESTAL.NOMBRE
HAVING  SUM(PLAN_PRESUPUESTAL.MOVIMIENTO + 
        PLAN_PRESUPUESTAL.MAN_CEN_CTO + 
        PLAN_PRESUPUESTAL.MAN_AUX_FUE+
        PLAN_PRESUPUESTAL.MAN_AUX_REF+
        PLAN_PRESUPUESTAL.MAN_AUX_GEN) <> 0
        AND 
        SUM((APROPIACION_CREDITO- APROPIACION_DEBITO)+ADICION + 
             REDUCCION +  
            (TRASLADO_CREDITO-TRASLADO_DEBITO) + 
            (APLAZAM_CREDITO-APLAZAM_DEBITO) +  
            (EJE_PPT_CREDITO-EJE_PPT_DEBITO)+MODIF_INGRESOS_EFECTIVO+MODIF_INGRESOS_PAPELES)<> 0
ORDER BY   SALDO_PLAN_PPTAL.ID    
