SELECT TIPO, TIPO_COMPROBANTE_NOM, 
       COMPROBANTE, 
       FECHA_COM FECHA, 
       NVL(DESCRIPCION,' ') DESCRIPCION, 
       NVL(TEXTO,' ') TEXTO, 
       CUENTANOMBRE,
       VALOR_DEBITO, 
       CUENTA,
       (VALOR_DEBITO - VALOR_CREDITO)-(DEBITO_AFECTADO - CREDITO_AFECTADO) + (MODIFICACION_DEBITO - MODIFICACION_CREDITO) SALDO,
       CASE WHEN CENTRO_COSTO_DET='99999999999999999999' THEN '' ELSE CENTRO_COSTO_DET END CODCENTRO,
       CASE WHEN FUENTE_RECURSO_DET='99999999999999999999' THEN '' ELSE FUENTE_RECURSO_DET END CODFUENTE,
       NOMBRE_DEP, 
       CARGOCOMPROBANTE,
       FECHA_VCN_DOC
FROM V_DETALLE_AUXILIAR_PPTAL
WHERE COMPANIA         = s$compania$s
  AND ANO                = s$ano$s
  AND TIPO               = 's$tipoCpte$s'
  AND COMPROBANTE BETWEEN s$numeroPptoInicial$s AND s$numeroPptoFinal$s