SELECT 
    LPAD(V_RESUMENPPTO_BASE.CODIGO,1) AS CLASE,
    V_RESUMENPPTO_BASE.ANO,
    V_RESUMENPPTO_BASE.CODIGO CUENTA,
    PLAN_PRESUPUESTAL.NOMBRE,
    CASE WHEN s$miles$s = 1 
    	THEN ROUND(SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE),-3)/1000
	ELSE SUM(V_RESUMENPPTO_BASE.APROPIACIONVIGENTE)
    END	AS APROPIACIONVIGENTE,
    SUM ( CASE WHEN V_RESUMENPPTO_BASE.MES = s$mes$s  
    	    THEN 
		CASE WHEN s$miles$s = 1
		THEN ROUND ((V_RESUMENPPTO_BASE.INGRESOSCAUSADOS + V_RESUMENPPTO_BASE.MODIFICACIONICA),-3)/1000 
		ELSE (V_RESUMENPPTO_BASE.INGRESOSCAUSADOS + V_RESUMENPPTO_BASE.MODIFICACIONICA) 
		END
          END	)DERECHOSMES,
    CASE WHEN s$miles$s = 1 
	THEN ROUND(SUM(V_RESUMENPPTO_BASE.INGRESOSCAUSADOS + V_RESUMENPPTO_BASE.MODIFICACIONICA),-3)/1000
	ELSE SUM(V_RESUMENPPTO_BASE.INGRESOSCAUSADOS + V_RESUMENPPTO_BASE.MODIFICACIONICA)
    END	AS DERECHOSACUM,
    SUM ( CASE WHEN V_RESUMENPPTO_BASE.MES = s$mes$s  
    	      THEN 
		CASE WHEN s$miles$s = 1 
		THEN ROUND ((V_RESUMENPPTO_BASE.INGRESOS_EFECTIVO + V_RESUMENPPTO_BASE.MODIF_INGRESOS_EFECTIVO),-3)/1000 
		ELSE (V_RESUMENPPTO_BASE.INGRESOS_EFECTIVO + V_RESUMENPPTO_BASE.MODIF_INGRESOS_EFECTIVO) 
		END
          END	)EFECTIVOMES,
    CASE WHEN s$miles$s = 1
      THEN ROUND(SUM(V_RESUMENPPTO_BASE.INGRESOS_EFECTIVO + V_RESUMENPPTO_BASE.MODIF_INGRESOS_EFECTIVO),-3)/1000
      ELSE SUM(V_RESUMENPPTO_BASE.INGRESOS_EFECTIVO + V_RESUMENPPTO_BASE.MODIF_INGRESOS_EFECTIVO)
    END	AS EFECTIVOACUM,
    SUM(CASE WHEN V_RESUMENPPTO_BASE.MES = s$mes$s  
          THEN 
	    CASE WHEN s$miles$s = 1
	    THEN ROUND ((V_RESUMENPPTO_BASE.INGRESOS_PAPELES + V_RESUMENPPTO_BASE.MODIF_INGRESOS_PAPELES),-3)/1000 
	    ELSE (V_RESUMENPPTO_BASE.INGRESOS_PAPELES + V_RESUMENPPTO_BASE.MODIF_INGRESOS_PAPELES) 
	    END
	END)PAPELESMES,
    CASE WHEN s$miles$s = 1
      THEN ROUND(SUM(V_RESUMENPPTO_BASE.INGRESOS_PAPELES + V_RESUMENPPTO_BASE.MODIF_INGRESOS_PAPELES),-3)/1000
      ELSE SUM(V_RESUMENPPTO_BASE.INGRESOS_PAPELES + V_RESUMENPPTO_BASE.MODIF_INGRESOS_PAPELES)
    END	AS PAPELESACUM,
   	CASE WHEN s$miles$s = 1
        THEN 
	  ROUND(
	    SUM ((V_RESUMENPPTO_BASE.INGRESOSCAUSADOS + V_RESUMENPPTO_BASE.MODIFICACIONICA) - 
          (V_RESUMENPPTO_BASE.INGRESOS_EFECTIVO + V_RESUMENPPTO_BASE.MODIF_INGRESOS_EFECTIVO)-
          (V_RESUMENPPTO_BASE.INGRESOS_PAPELES + V_RESUMENPPTO_BASE.MODIF_INGRESOS_PAPELES)), -3)/1000
	ELSE 
	  SUM((V_RESUMENPPTO_BASE.INGRESOSCAUSADOS + V_RESUMENPPTO_BASE.MODIFICACIONICA) - 
        (V_RESUMENPPTO_BASE.INGRESOS_EFECTIVO + V_RESUMENPPTO_BASE.MODIF_INGRESOS_EFECTIVO)-
        (V_RESUMENPPTO_BASE.INGRESOS_PAPELES + V_RESUMENPPTO_BASE.MODIF_INGRESOS_PAPELES))
        END PENDIENTECOBRO
  FROM V_RESUMENPPTO_BASE
    INNER JOIN PLAN_PRESUPUESTAL
    ON V_RESUMENPPTO_BASE.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
    AND V_RESUMENPPTO_BASE.ANO = PLAN_PRESUPUESTAL.ANO
    AND V_RESUMENPPTO_BASE.ID = PLAN_PRESUPUESTAL.CODIGO
	WHERE V_RESUMENPPTO_BASE.COMPANIA = s$compania$s
		AND V_RESUMENPPTO_BASE.ANO = s$ano$s
		AND V_RESUMENPPTO_BASE.ID BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
		AND V_RESUMENPPTO_BASE.MES <= s$mes$s
		AND V_RESUMENPPTO_BASE.NATURALEZA = 'C'
		AND LENGTH(V_RESUMENPPTO_BASE.CODIGO) BETWEEN 0 AND NVL(s$nivel$s,6)
	AND V_RESUMENPPTO_BASE.INFORME NOT IN(0)
GROUP BY V_RESUMENPPTO_BASE.ANO,
        V_RESUMENPPTO_BASE.CODIGO,
        V_RESUMENPPTO_BASE.ID, 
        PLAN_PRESUPUESTAL.NOMBRE
