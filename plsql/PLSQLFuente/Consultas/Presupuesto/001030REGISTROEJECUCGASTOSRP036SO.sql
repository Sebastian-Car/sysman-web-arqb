SELECT SUBSTR(SALDO_PLAN_PPTAL.CODIGO,1,1) AS CLASE,
	SALDO_PLAN_PPTAL.CODIGO CUENTA,
	PLAN_PRESUPUESTAL.NOMBRE,
	SUM(CASE WHEN s$miles$s = 1
			THEN ROUND(CASE WHEN PLAN_PRESUPUESTAL.TIPOVIGENCIA ='RC' OR PLAN_PRESUPUESTAL.TIPOVIGENCIA ='RA'
							THEN (SALDO_PLAN_PPTAL.APROPIACION_DEBITO-SALDO_PLAN_PPTAL.APROPIACION_CREDITO)+ 
								SALDO_PLAN_PPTAL.ADICION + (TRASLADO_DEBITO + APLAZAM_DEBITO)  + SALDO_PLAN_PPTAL.REDUCCION -
								(TRASLADO_CREDITO+APLAZAM_CREDITO)
							ELSE SALDO_PLAN_PPTAL.REG_CONTRACT + SALDO_PLAN_PPTAL.REG_NO_CONTRACT + 
								SALDO_PLAN_PPTAL.REG_REVERSION + SALDO_PLAN_PPTAL.MODIF_REG_CONT + SALDO_PLAN_PPTAL.MODIF_REG_NOCONT
						
						END,-3)/1000
			ELSE CASE WHEN PLAN_PRESUPUESTAL.TIPOVIGENCIA ='RC' OR PLAN_PRESUPUESTAL.TIPOVIGENCIA ='RA'
					THEN (SALDO_PLAN_PPTAL.APROPIACION_DEBITO-SALDO_PLAN_PPTAL.APROPIACION_CREDITO)+ 
						SALDO_PLAN_PPTAL.ADICION + (TRASLADO_DEBITO + APLAZAM_DEBITO)  + SALDO_PLAN_PPTAL.REDUCCION -
						(TRASLADO_CREDITO+APLAZAM_CREDITO)
					ELSE SALDO_PLAN_PPTAL.REG_CONTRACT + SALDO_PLAN_PPTAL.REG_NO_CONTRACT + 
						SALDO_PLAN_PPTAL.REG_REVERSION + SALDO_PLAN_PPTAL.MODIF_REG_CONT + SALDO_PLAN_PPTAL.MODIF_REG_NOCONT
				END
		END) COMPROMISOSACUM,
	SUM(CASE WHEN SALDO_PLAN_PPTAL.MES = s$mes$s
		THEN 
			CASE WHEN s$miles$s = 1
				THEN ROUND(SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION + SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION,-3)/1000
				ELSE SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION + SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION
			END
		END) AS OBLIGACIONESDELMES,
	SUM(CASE WHEN s$miles$s = 1
			THEN ROUND(SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION + SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION,-3)/1000
			ELSE SALDO_PLAN_PPTAL.REGISTRO_OBLIGACION + SALDO_PLAN_PPTAL.MODIF_REGISTRO_OBLIGACION
		END)  AS OBLIGACIONESACUM,
	SUM(CASE WHEN SALDO_PLAN_PPTAL.MES = s$mes$s 
			THEN 
				CASE WHEN s$miles$s = 1
					THEN ROUND((SALDO_PLAN_PPTAL.EJE_PPT_DEBITO-SALDO_PLAN_PPTAL.EJE_PPT_CREDITO),-3)/1000
					ELSE (SALDO_PLAN_PPTAL.EJE_PPT_DEBITO-SALDO_PLAN_PPTAL.EJE_PPT_CREDITO)
				END
		END) PAGOSMES,
	SUM(CASE WHEN s$miles$s = 1
					THEN ROUND((SALDO_PLAN_PPTAL.EJE_PPT_DEBITO-SALDO_PLAN_PPTAL.EJE_PPT_CREDITO),-3)/1000
					ELSE (SALDO_PLAN_PPTAL.EJE_PPT_DEBITO - SALDO_PLAN_PPTAL.EJE_PPT_CREDITO)
		END)PAGOSACUM	
FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
	INNER JOIN V_SALDO_PLAN_PPTAL SALDO_PLAN_PPTAL
		ON PLAN_PRESUPUESTAL.COMPANIA    = SALDO_PLAN_PPTAL.COMPANIA 
		AND PLAN_PRESUPUESTAL.ANO        = SALDO_PLAN_PPTAL.ANO
		AND PLAN_PRESUPUESTAL.ID         = SALDO_PLAN_PPTAL.ID
WHERE  SALDO_PLAN_PPTAL.COMPANIA = s$compania$s
	AND SALDO_PLAN_PPTAL.ANO     = s$ano$s
	AND SALDO_PLAN_PPTAL.MES  <= s$mes$s
	AND SALDO_PLAN_PPTAL.ID BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
	AND PLAN_PRESUPUESTAL.NATURALEZA = 'D'
	AND LENGTH(SALDO_PLAN_PPTAL.ID) BETWEEN 0 AND NVL(s$nivel$s,6)
GROUP BY SUBSTR(SALDO_PLAN_PPTAL.CODIGO,1,1),
		SALDO_PLAN_PPTAL.CODIGO,
		PLAN_PRESUPUESTAL.NOMBRE
ORDER BY SALDO_PLAN_PPTAL.CODIGO
