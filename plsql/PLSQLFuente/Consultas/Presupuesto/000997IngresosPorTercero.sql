SELECT
  DETALLE_COMPROBANTE_PPTAL.COMPANIA,
  TERCERO.NOMBRE        					NOMBRETER,
  TERCERO.NIT_CEDULA    				CODEQUIV,
  TERCERO.NIT           					NITTERCERO,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE, 
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE, 
  TO_CHAR(DETALLE_COMPROBANTE_PPTAL.FECHA,'DD/MM/YYYY') 	FECHA, 
  DETALLE_COMPROBANTE_PPTAL.CUENTA 			CTAPPTAL,
  PLAN_PRESUPUESTAL.NOMBRE                			NOMBREPPTAL,
  DETALLE_COMPROBANTE_PPTAL.DESCRIPCION 			CONCEPTO,
  VALOR_CREDITO-VALOR_DEBITO        			VALORINGRESO,
  DETALLE_COMPROBANTE_PPTAL.TERCERO
FROM ((DETALLE_COMPROBANTE_PPTAL
LEFT JOIN PLAN_PRESUPUESTAL ON
  DETALLE_COMPROBANTE_PPTAL.COMPANIA     = PLAN_PRESUPUESTAL.COMPANIA AND
  DETALLE_COMPROBANTE_PPTAL.ANO          = PLAN_PRESUPUESTAL.ANO AND
  DETALLE_COMPROBANTE_PPTAL.CUENTA = PLAN_PRESUPUESTAL.CODIGO)
LEFT JOIN TERCERO ON
  DETALLE_COMPROBANTE_PPTAL.COMPANIA = TERCERO.COMPANIA AND
  DETALLE_COMPROBANTE_PPTAL.TERCERO  = TERCERO.NIT AND
  DETALLE_COMPROBANTE_PPTAL.SUCURSAL  = TERCERO.SUCURSAL)
LEFT JOIN TIPO_COMPROBPP ON
  DETALLE_COMPROBANTE_PPTAL.COMPANIA  = TIPO_COMPROBPP.COMPANIA AND
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE  = TIPO_COMPROBPP.CODIGO
WHERE TIPO_COMPROBPP.CLASE           ='ING'
AND DETALLE_COMPROBANTE_PPTAL.COMPANIA=s$compania$s
AND DETALLE_COMPROBANTE_PPTAL.TERCERO 
    BETWEEN 's$terceroInicial$s' AND 's$terceroFinal$s'
AND FECHA
    BETWEEN s$fechaInicial$s AND s$fechaFinal$s
ORDER BY 
  DETALLE_COMPROBANTE_PPTAL.ANO,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
  DETALLE_COMPROBANTE_PPTAL.CONSECUTIVO
