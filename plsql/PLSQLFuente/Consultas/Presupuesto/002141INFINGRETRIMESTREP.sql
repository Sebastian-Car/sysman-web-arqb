MERGE INTO CONSULTAS FIN USING (SELECT '002141INFINGRETRIMESTREP' INFORME ,TO_CLOB(q'[SELECT 
  ID,
  CODIGO,
  NOMBRE,
  TO_CHAR(DATO1, 'FM999G999G999G990D00') AS DATO1,
  TO_CHAR(DATO2, 'FM999G999G999G990D00') AS DATO2,
  TO_CHAR(DATO3, 'FM999G999G999G990D00') AS DATO3,
  TO_CHAR(SUM(DATO1 + DATO2 + DATO3), 'FM999G999G999G990D00') AS TOTAL
  FROM (SELECT * FROM (
  SELECT PLAN_PRESUPUESTAL.ID,
    PLAN_PRESUPUESTAL.CODIGO,
    PLAN_PRESUPUESTAL.NOMBRE,
    CASE WHEN NATURALEZA='D'
    THEN (SALDO_PLAN_PPTAL.EJE_PPT_DEBITO-SALDO_PLAN_PPTAL.EJE_PPT_CREDITO)+MODIF_INGRESOS
    ELSE (SALDO_PLAN_PPTAL.EJE_PPT_CREDITO-SALDO_PLAN_PPTAL.EJE_PPT_DEBITO)+MODIF_INGRESOS END AS TOTAL,
    SALDO_PLAN_PPTAL.MES
  FROM V_PLAN_PRESUPUESTAL PLAN_PRESUPUESTAL
  LEFT JOIN SALDO_PLAN_PPTAL
  ON PLAN_PRESUPUESTAL.ID        = SALDO_PLAN_PPTAL.CODIGO
  AND PLAN_PRESUPUESTAL.ANO      = SALDO_PLAN_PPTAL.ANO
  AND PLAN_PRESUPUESTAL.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
  WHERE SALDO_PLAN_PPTAL.MES BETWEEN s$mesInicial$s AND s$mesFinal$s
  AND PLAN_PRESUPUESTAL.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  AND PLAN_PRESUPUESTAL.NATURALEZA IN ('C')
  AND PLAN_PRESUPUESTAL.COMPANIA    =s$compania$s
  AND PLAN_PRESUPUESTAL.ANO         =s$anio$s
  GROUP BY PLAN_PRESUPUESTAL.ID,
    PLAN_PRESUPUESTAL.CODIGO,
    PLAN_PRESUPUESTAL.NOMBRE,
    PLAN_PRESUPUESTAL.ANO,
    SALDO_PLAN_PPTAL.MES,
    PLAN_PRESUPUESTAL.NATURALEZA,
    SALDO_PLAN_PPTAL.EJE_PPT_DEBITO,
    SALDO_PLAN_PPTAL.EJE_PPT_CREDITO,
    MODIF_INGRESOS
) CONS
 PIVOT(
  SUM(TOTAL) FOR MES IN ('s$mesInicial$s' "DATO1", 's$mesIntermedio$s' "DATO2", 's$mesFinal$s' "DATO3" )
  )) GROUP BY ID,CODIGO,NOMBRE,DATO1,DATO2,DATO3
  ORDER BY 2]') CONSULTA, 3 APLICACION ,TO_CLOB(q'[]') CONSULTA_OPCIONAL, NULL CREATED_BY, NULL MODIFIED_BY  FROM DUAL ) INI ON (INI.INFORME = FIN.INFORME )  WHEN MATCHED THEN  UPDATE SET FIN.CONSULTA =  INI.CONSULTA, FIN.APLICACION =  INI.APLICACION, FIN.CONSULTA_OPCIONAL =  INI.CONSULTA_OPCIONAL, FIN.MODIFIED_BY = INI.MODIFIED_BY, FIN.DATE_MODIFIED = SYSDATE  WHEN NOT MATCHED THEN  INSERT (INFORME,CONSULTA, APLICACION,CONSULTA_OPCIONAL,CREATED_BY,DATE_CREATED)  VALUES (INI.INFORME,INI.CONSULTA, INI.APLICACION,INI.CONSULTA_OPCIONAL,INI.CREATED_BY,SYSDATE);