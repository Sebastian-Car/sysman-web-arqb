SELECT   DETALLE_COMPROBANTE_PPTAL.FECHA,
         TO_CHAR(DETALLE_COMPROBANTE_PPTAL.FECHA, 'MM') MES,
         TO_CHAR(DETALLE_COMPROBANTE_PPTAL.FECHA, 'DD') DIA,
         TO_CHAR(DETALLE_COMPROBANTE_PPTAL.FECHA, 'YYYY') ANO,
         DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
         TIPO_COMPROBPP.CLASE,
         
         (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) VALORTOTAL,
         (CASE WHEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) < 0 THEN '-' ELSE '+' END) SIGNO,
         ABS((DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO))  VALOR,
        ((CASE WHEN (CASE WHEN TIPO_COMPROBPP.CLASE IN ('REO', 'MRO', 'ARO', 'DRO') THEN '7' ELSE '' END) || 
        (CASE WHEN TIPO_COMPROBPP.CLASE IN ('EGR', 'AEG', 'DEG') THEN '8' ELSE '' END) = '7' THEN 'CPC' ELSE '' END) ||
        (CASE WHEN (CASE WHEN TIPO_COMPROBPP.CLASE IN ('REO', 'MRO', 'ARO', 'DRO') THEN '7' ELSE '' END) ||
        (CASE WHEN  TIPO_COMPROBPP.CLASE IN ('EGR', 'AEG', 'DEG') THEN '8' ELSE '' END) = '8' THEN 'P' ELSE '' END)) CLASELETRAS,
         (CASE WHEN TIPO_COMPROBPP.CLASE IN ('REO', 'MRO', 'ARO', 'DRO') THEN '7' ELSE ''END) || (CASE WHEN TIPO_COMPROBPP.CLASE IN ('EGR','AEG','DEG') THEN '8' ELSE '' END) CLASECOMP,
   
         (CASE WHEN (CASE WHEN TIPO_COMPROBPP.CLASE = 'REO' OR TIPO_COMPROBPP.CLASE = 'MRO' OR TIPO_COMPROBPP.CLASE = 'ARO' OR TIPO_COMPROBPP.CLASE = 'DRO' THEN '7' ELSE '' END) ||
         (CASE WHEN TIPO_COMPROBPP.CLASE = 'EGR' OR TIPO_COMPROBPP.CLASE = 'AEG' OR TIPO_COMPROBPP.CLASE = 'DEG' THEN '8' ELSE '' END) = '7' THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) ELSE 0 END) CXPCONSTITUIDAS,
            
         (CASE WHEN (CASE WHEN TIPO_COMPROBPP.CLASE = 'REO' OR TIPO_COMPROBPP.CLASE = 'MRO' OR TIPO_COMPROBPP.CLASE = 'ARO' OR TIPO_COMPROBPP.CLASE = 'DRO' THEN '7' ELSE '' END) ||
         (CASE WHEN TIPO_COMPROBPP.CLASE = 'EGR' OR TIPO_COMPROBPP.CLASE = 'AEG' OR TIPO_COMPROBPP.CLASE = 'DEG' THEN '8' ELSE '' END) = '8' THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) ELSE 0 END)TOTALPAGOS,
 
         V_PLAN_PRESUPUESTAL.NOMBRE,
         V_PLAN_PRESUPUESTAL.NIVEL1,
         V_PLAN_PRESUPUESTAL.NIVEL2,
         V_PLAN_PRESUPUESTAL.NIVEL3,
         V_PLAN_PRESUPUESTAL.NIVEL4,
         V_PLAN_PRESUPUESTAL.NIVEL5,
         V_PLAN_PRESUPUESTAL.NIVEL6,
         V_PLAN_PRESUPUESTAL.NIVEL7,
         V_PLAN_PRESUPUESTAL.ID,
         DECODE(V_PLAN_PRESUPUESTAL.DESTINO, 'F', 'Funcionamiento',
                                             'I', 'Inversión', 
                                             'S', 'Servicio de la Deuda') DESTINO,
          (CASE WHEN V_PLAN_PRESUPUESTAL.DESTINO = 'I' THEN 's$nombreNivel1I$s' ELSE 's$nombreNivel1F$s' END) NOMBRENIVEL1,
         (CASE WHEN V_PLAN_PRESUPUESTAL.DESTINO = 'I' THEN 's$nombreNivel2I$s' ELSE 's$nombreNivel2F$s' END) NOMBRENIVEL2,
         (CASE WHEN V_PLAN_PRESUPUESTAL.DESTINO = 'I' THEN 's$nombreNivel3I$s' ELSE 's$nombreNivel3F$s' END) NOMBRENIVEL3,
         (CASE WHEN V_PLAN_PRESUPUESTAL.DESTINO = 'I' THEN 's$nombreNivel4I$s' ELSE 's$nombreNivel4F$s' END) NOMBRENIVEL4,
         (CASE WHEN V_PLAN_PRESUPUESTAL.DESTINO = 'I' THEN 's$nombreNivel5I$s' ELSE 's$nombreNivel5F$s' END) NOMBRENIVEL5,
         (CASE WHEN V_PLAN_PRESUPUESTAL.DESTINO = 'I' THEN 's$nombreNivel6I$s' ELSE 's$nombreNivel6F$s' END) NOMBRENIVEL6,
         V_PLAN_PRESUPUESTAL.AUXILIAR,
         DETALLE_COMPROBANTE_PPTAL.DESCRIPCION,
         DECODE(V_PLAN_PRESUPUESTAL.CENTRO_COSTO, '99999999999999999999', '') CENTRO_COSTO
  FROM   DETALLE_COMPROBANTE_PPTAL
  LEFT JOIN TIPO_COMPROBPP 
   ON    DETALLE_COMPROBANTE_PPTAL.COMPANIA    = TIPO_COMPROBPP.COMPANIA
    AND  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = TIPO_COMPROBPP.CODIGO
  LEFT JOIN V_PLAN_PRESUPUESTAL
   ON    DETALLE_COMPROBANTE_PPTAL.COMPANIA    = V_PLAN_PRESUPUESTAL.COMPANIA
    AND  DETALLE_COMPROBANTE_PPTAL.ANO         = V_PLAN_PRESUPUESTAL.ANO
    AND  DETALLE_COMPROBANTE_PPTAL.ID= V_PLAN_PRESUPUESTAL.ID 
  INNER JOIN COMPROBANTE_PPTAL 
   ON    DETALLE_COMPROBANTE_PPTAL.COMPANIA    = COMPROBANTE_PPTAL.COMPANIA 
    AND  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO
    AND  DETALLE_COMPROBANTE_PPTAL.ANO         = COMPROBANTE_PPTAL.ANO 
    AND  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = COMPROBANTE_PPTAL.TIPO 
WHERE  V_PLAN_PRESUPUESTAL.COMPANIA = s$compania$s
    AND  V_PLAN_PRESUPUESTAL.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s' 
    s$condicionFuenteRec$s
    s$condicionCentroCosto$s
    AND  DETALLE_COMPROBANTE_PPTAL.ANO = s$ano$s
    AND  TO_CHAR(DETALLE_COMPROBANTE_PPTAL.FECHA, 'MM') BETWEEN 's$mesInicial$s' AND 's$mesFinal$s'
    AND  LENGTH(DETALLE_COMPROBANTE_PPTAL.CUENTA) BETWEEN 0 AND s$nivel$s   
    AND  (TIPO_COMPROBPP.CLASE = 'REO' OR  TIPO_COMPROBPP.CLASE = 'EGR')   
 ORDER BY V_PLAN_PRESUPUESTAL.CODIGO
