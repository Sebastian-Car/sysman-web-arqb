SELECT VISTA.TIPO_COMPROBANTE_NOM,
  VISTA.COMPROBANTE,
  VISTA.COMPROBANTE TIPO_CPTE,
  VISTA.FECHA_COM FECHA, 
  VISTA.TERCERO_COM TERCERO, 
  VISTA.SUCURSAL_COM SUCURSAL, 
  VISTA.TERCERO_NOM, 
  VISTA.CUENTA,
  VISTA.CUENTANOMBRE,
  (
  CASE
    WHEN VISTA.VALOR_DEBITO = 0
    THEN VISTA.VALOR_CREDITO
    ELSE VISTA.VALOR_DEBITO
  END) VALORCOMPROBANTE,
  COMPROBANTE_PPTALAFECTADOS.TIPO_CPTE_AFECT,
  COMPROBANTE_PPTALAFECTADOS.COMPROBANTE_AFECT CMPTE_AFECTADO,
  VISTA.DESCRIPCION
FROM V_DETALLE_AUXILIAR_PPTAL VISTA
INNER JOIN COMPROBANTE_PPTALAFECTADOS
ON VISTA.COMPANIA                       = COMPROBANTE_PPTALAFECTADOS.COMPANIA
AND VISTA.ANO                           = COMPROBANTE_PPTALAFECTADOS.ANO
AND VISTA.TIPO                          = COMPROBANTE_PPTALAFECTADOS.TIPO_CPTE
AND VISTA.COMPROBANTE                   = COMPROBANTE_PPTALAFECTADOS.COMPROBANTE
WHERE VISTA.COMPANIA       = s$compania$s
  AND VISTA.ANO            = s$ano$s
  AND VISTA.TIPO           = 's$tipoCpte$s'
  AND VISTA.COMPROBANTE BETWEEN s$numeroPptoInicial$s AND s$numeroPptoFinal$s
