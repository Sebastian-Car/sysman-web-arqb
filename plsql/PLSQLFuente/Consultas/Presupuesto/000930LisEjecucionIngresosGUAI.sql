SELECT  	V_SALDO_PLAN_PPTAL.COMPANIA,
            V_SALDO_PLAN_PPTAL.ANO,
            V_SALDO_PLAN_PPTAL.ID CUENTA,
            V_PLAN_PRESUPUESTAL.NOMBRE,
			SUM(V_SALDO_PLAN_PPTAL.APROPIACION_CREDITO - V_SALDO_PLAN_PPTAL.APROPIACION_DEBITO) APROPIADO,
            SUM(V_SALDO_PLAN_PPTAL.APROPIACION_CREDITO - V_SALDO_PLAN_PPTAL.APROPIACION_DEBITO + V_SALDO_PLAN_PPTAL.ADICION +  V_SALDO_PLAN_PPTAL.REDUCCION)TOTALPRESUPUESTOF,
            SUM(V_SALDO_PLAN_PPTAL.ADICION)  TOTALADICIONES,
            SUM(V_SALDO_PLAN_PPTAL.REDUCCION)  TOTALREDUCCIONES,
			SUM(CASE WHEN V_SALDO_PLAN_PPTAL.MES < s$mesFinal$s
				THEN V_SALDO_PLAN_PPTAL.INGRESOS_CAUSADOS + V_SALDO_PLAN_PPTAL.MODIF_INGRESOS_CAUSADOS
				END) CAUSACIONACUMULADA,
			SUM(CASE WHEN V_SALDO_PLAN_PPTAL.MES = s$mesFinal$s 
				THEN V_SALDO_PLAN_PPTAL.INGRESOS_CAUSADOS + V_SALDO_PLAN_PPTAL.MODIF_INGRESOS_CAUSADOS
				END)CAUSACIONPERIODO,
       SUM(V_SALDO_PLAN_PPTAL.INGRESOS_CAUSADOS + V_SALDO_PLAN_PPTAL.MODIF_INGRESOS_CAUSADOS)CAUSACIONESANTERIORES, 
			SUM(CASE WHEN V_SALDO_PLAN_PPTAL.MES < s$mesFinal$s 
				THEN (V_SALDO_PLAN_PPTAL.EJE_PPT_CREDITO - V_SALDO_PLAN_PPTAL.EJE_PPT_DEBITO)+ V_SALDO_PLAN_PPTAL.MODIF_INGRESOS 
				END)AS RECAUDOSACUMULADOS,
			SUM(CASE WHEN V_SALDO_PLAN_PPTAL.MES = s$mesFinal$s
				THEN EJE_PPT_CREDITO - EJE_PPT_DEBITO + MODIF_INGRESOS_EFECTIVO + MODIF_INGRESOS_PAPELES
				END)RECAUDOSMES,
			SUM(EJE_PPT_CREDITO - EJE_PPT_DEBITO + MODIF_INGRESOS_EFECTIVO + MODIF_INGRESOS_PAPELES)RECAUDOSANTERIORES,
			SUM(V_SALDO_PLAN_PPTAL.REDUCCION - ((EJE_PPT_CREDITO - EJE_PPT_DEBITO) + MODIF_INGRESOS_EFECTIVO + MODIF_INGRESOS_PAPELES))PORECAUDAR,
			SUM(CASE WHEN V_SALDO_PLAN_PPTAL.MES < s$mesFinal$s 
				THEN (V_SALDO_PLAN_PPTAL.INGRESOS_CAUSADOS + V_SALDO_PLAN_PPTAL.MODIF_INGRESOS_CAUSADOS)+
                     (V_SALDO_PLAN_PPTAL.EJE_PPT_CREDITO - V_SALDO_PLAN_PPTAL.EJE_PPT_DEBITO)+ V_SALDO_PLAN_PPTAL.MODIF_INGRESOS 
				END)PORCOBRAR,
			SUM(CASE WHEN V_SALDO_PLAN_PPTAL.MES = s$mesFinal$s
				THEN V_SALDO_PLAN_PPTAL.ADICION
				END)ADICIONMES,
			SUM(CASE WHEN V_SALDO_PLAN_PPTAL.MES = s$mesFinal$s
				THEN V_SALDO_PLAN_PPTAL.REDUCCION
				END)REDUCCIONMES	
FROM V_PLAN_PRESUPUESTAL
    INNER JOIN V_SALDO_PLAN_PPTAL
        ON V_PLAN_PRESUPUESTAL.COMPANIA = V_SALDO_PLAN_PPTAL.COMPANIA 
        AND V_PLAN_PRESUPUESTAL.ANO     = V_SALDO_PLAN_PPTAL.ANO
        AND V_PLAN_PRESUPUESTAL.ID      = V_SALDO_PLAN_PPTAL.ID
WHERE V_SALDO_PLAN_PPTAL.COMPANIA = s$compania$s
      AND V_SALDO_PLAN_PPTAL.ANO = s$ano$s
      AND V_SALDO_PLAN_PPTAL.MES   BETWEEN 0 AND s$mesFinal$s
      AND V_SALDO_PLAN_PPTAL.ID BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s    
      AND V_PLAN_PRESUPUESTAL.NATURALEZA = 'C'
      AND V_PLAN_PRESUPUESTAL.AUXILIAR  IS NULL
      AND V_PLAN_PRESUPUESTAL.CENTRO_COSTO IS NULL
    GROUP BY  V_SALDO_PLAN_PPTAL.COMPANIA,
              V_SALDO_PLAN_PPTAL.ANO,
              V_SALDO_PLAN_PPTAL.ID,
              V_PLAN_PRESUPUESTAL.NOMBRE
