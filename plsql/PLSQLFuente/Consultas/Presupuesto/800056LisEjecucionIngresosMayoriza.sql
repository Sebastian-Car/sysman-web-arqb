SELECT TEMPORAL.COMPANIA,
       V_PLAN_PRESUPUESTAL.CODIGO,
       V_PLAN_PRESUPUESTAL.NOMBRE,
       V_PLAN_PRESUPUESTAL.MOVIMIENTO,
       SUM(TEMPORAL.APROPIADO)APROPIADO,
       SUM(TEMPORAL.ADICIONES_ANTERIORES)ADICIONES_ANTERIORES,
       SUM(TEMPORAL.ADICIONES_MES)ADICIONES_MES,
       SUM(TEMPORAL.TOTAL_ADICIONES)TOTAL_ADICIONES,
       SUM(TEMPORAL.REDUCCIONES_ANTERIORES)REDUCCIONES_ANTERIORES,
       SUM(TEMPORAL.REDUCCIONES_MES)REDUCCIONES_MES,
       SUM(TEMPORAL.TOTAL_REDUCCIONES)TOTAL_REDUCCIONES,
       SUM(TEMPORAL.TOTAL_PRESUPUESTO)TOTAL_PRESUPUESTO,
       SUM(TEMPORAL.TOTAL_RECONOCIMIENTOS)TOTAL_RECONOCIMIENTOS,
       SUM(TEMPORAL.RECONOCIMIENTO_MES)RECONOCIMIENTO_MES,
       SUM(TEMPORAL.RECONOCIMIENTOS_ANTERIORES)RECONOCIMIENTOS_ANTERIORES,
       SUM(TEMPORAL.TOTAL_RECAUDOS)TOTAL_RECAUDOS,
       SUM(TEMPORAL.RECAUDOS_MES)RECAUDOS_MES,
       SUM(TEMPORAL.RECAUDOS_ANTERIORES)RECAUDOS_ANTERIORES,
       SUM(TEMPORAL.SALDO_POR_RECAUDAR)SALDO_POR_RECAUDAR,
       SUM(TEMPORAL.CUENTAS_POR_COBRAR)CUENTAS_POR_COBRAR,
       V_PLAN_PRESUPUESTAL.CONSITUACIONFONDOS,
       V_PLAN_PRESUPUESTAL.FUENTE_RECURSO FUENTE
FROM( 
WITH EJECINGRESOS_GUACENTROCOSTO AS (
      SELECT  V_SALDO_PLAN_PPTAL.COMPANIA,
              V_SALDO_PLAN_PPTAL.ANO,
              V_SALDO_PLAN_PPTAL.ID CUENTA,
              V_PLAN_PRESUPUESTAL.ID,
              V_PLAN_PRESUPUESTAL.CENTRO_COSTO,
              V_PLAN_PRESUPUESTAL.NATURALEZA,
              V_PLAN_PRESUPUESTAL.CODIGO,
              V_PLAN_PRESUPUESTAL.NOMBRE,
              V_PLAN_PRESUPUESTAL.MOVIMIENTO,
              V_PLAN_PRESUPUESTAL.CONSITUACIONFONDOS,
              V_PLAN_PRESUPUESTAL.FUENTE_RECURSO,
              V_PLAN_PRESUPUESTAL.DESTINO,
              SUM(APROPIACION_CREDITO-APROPIACION_DEBITO) AS APROPIADO,
              SUM(V_SALDO_PLAN_PPTAL.ADICION)AS ADICIONA,
              SUM(V_SALDO_PLAN_PPTAL.REDUCCION)AS REDUCCIONA,
             (SUM(APROPIACION_CREDITO-APROPIACION_DEBITO)+ SUM(V_SALDO_PLAN_PPTAL.ADICION)  + 
              SUM(V_SALDO_PLAN_PPTAL.REDUCCION) +  SUM(TRASLADO_CREDITO-TRASLADO_DEBITO)+
              SUM(APLAZAM_CREDITO - APLAZAM_DEBITO)) AS TOTALPRESUPUESTO,
             (SUM(APROPIACION_CREDITO-APROPIACION_DEBITO)+ SUM(V_SALDO_PLAN_PPTAL.ADICION)  + 
              SUM(V_SALDO_PLAN_PPTAL.REDUCCION))AS TOTALPRESUPUESTOS,
             (SUM(EJE_PPT_CREDITO-EJE_PPT_DEBITO) + MODIF_INGRESOS) AS RECAUDOSACUMULADOS,
             (SUM(APROPIACION_CREDITO-APROPIACION_DEBITO)+SUM(V_SALDO_PLAN_PPTAL.ADICION)  + 
              SUM(V_SALDO_PLAN_PPTAL.REDUCCION)+ SUM(TRASLADO_CREDITO-TRASLADO_DEBITO)+
              SUM(APLAZAM_CREDITO - APLAZAM_DEBITO ))-
             (SUM(EJE_PPT_CREDITO-EJE_PPT_DEBITO) + MODIF_INGRESOS)AS PORRECAUDAR,
              SUM(INGRESOS_CAUSADOS +(MODIF_INGRESOS_CAUSADOS))AS CAUSACIONACUMULADA
      FROM V_PLAN_PRESUPUESTAL
        INNER JOIN V_SALDO_PLAN_PPTAL
          ON  V_PLAN_PRESUPUESTAL.COMPANIA = V_SALDO_PLAN_PPTAL.COMPANIA 
          AND V_PLAN_PRESUPUESTAL.ANO      = V_SALDO_PLAN_PPTAL.ANO
          AND V_PLAN_PRESUPUESTAL.ID       = V_SALDO_PLAN_PPTAL.ID
      WHERE V_SALDO_PLAN_PPTAL.COMPANIA = s$compania$s
        AND V_SALDO_PLAN_PPTAL.ANO      = s$ano$s
        AND V_SALDO_PLAN_PPTAL.MES < s$mesFinal$s
        AND V_PLAN_PRESUPUESTAL.NATURALEZA  = 'C'
        AND V_SALDO_PLAN_PPTAL.ID BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
        AND V_PLAN_PRESUPUESTAL.CENTRO_COSTO = s$centroCosto$s
      GROUP BY  V_SALDO_PLAN_PPTAL.COMPANIA,
                V_SALDO_PLAN_PPTAL.ANO,
                V_SALDO_PLAN_PPTAL.ID,
                V_PLAN_PRESUPUESTAL.ID,
                V_PLAN_PRESUPUESTAL.CENTRO_COSTO,
                V_PLAN_PRESUPUESTAL.NATURALEZA,
                V_PLAN_PRESUPUESTAL.CODIGO,
                V_PLAN_PRESUPUESTAL.NOMBRE,
                V_PLAN_PRESUPUESTAL.MOVIMIENTO,
                V_PLAN_PRESUPUESTAL.CONSITUACIONFONDOS,
                V_PLAN_PRESUPUESTAL.FUENTE_RECURSO,
                V_PLAN_PRESUPUESTAL.DESTINO,
                MODIF_INGRESOS)
SELECT  EJECINGRESOS_GUACENTROCOSTO.COMPANIA,
        EJECINGRESOS_GUACENTROCOSTO.ANO,
        EJECINGRESOS_GUACENTROCOSTO.CUENTA,
        EJECINGRESOS_GUACENTROCOSTO.CODIGO,
        EJECINGRESOS_GUACENTROCOSTO.NOMBRE,
        EJECINGRESOS_GUACENTROCOSTO.MOVIMIENTO,
        EJECINGRESOS_GUACENTROCOSTO.APROPIADO,
        EJECINGRESOS_GUACENTROCOSTO.ADICIONA ADICIONES_ANTERIORES,
        V_SALDO_PLAN_PPTAL.ADICION AS ADICIONES_MES,
        EJECINGRESOS_GUACENTROCOSTO.ADICIONA + V_SALDO_PLAN_PPTAL.ADICION TOTAL_ADICIONES,
        EJECINGRESOS_GUACENTROCOSTO.REDUCCIONA REDUCCIONES_ANTERIORES,
        V_SALDO_PLAN_PPTAL.REDUCCION  REDUCCIONES_MES,
        EJECINGRESOS_GUACENTROCOSTO.REDUCCIONA + V_SALDO_PLAN_PPTAL.REDUCCION TOTAL_REDUCCIONES,
        EJECINGRESOS_GUACENTROCOSTO.TOTALPRESUPUESTO TOTAL_PRESUPUESTO ,
        EJECINGRESOS_GUACENTROCOSTO.CAUSACIONACUMULADA + (INGRESOS_CAUSADOS +(MODIF_INGRESOS_CAUSADOS))   TOTAL_RECONOCIMIENTOS,
        INGRESOS_CAUSADOS +(MODIF_INGRESOS_CAUSADOS) RECONOCIMIENTO_MES,       
        EJECINGRESOS_GUACENTROCOSTO.CAUSACIONACUMULADA RECONOCIMIENTOS_ANTERIORES,
        EJECINGRESOS_GUACENTROCOSTO.RECAUDOSACUMULADOS  + 
       (EJE_PPT_CREDITO - EJE_PPT_DEBITO + MODIF_INGRESOS_EFECTIVO + MODIF_INGRESOS_PAPELES) TOTAL_RECAUDOS,
        EJE_PPT_CREDITO - EJE_PPT_DEBITO + MODIF_INGRESOS_EFECTIVO + MODIF_INGRESOS_PAPELES RECAUDOS_MES ,
        EJECINGRESOS_GUACENTROCOSTO.RECAUDOSACUMULADOS  RECAUDOS_ANTERIORES,
        EJECINGRESOS_GUACENTROCOSTO.PORRECAUDAR SALDO_POR_RECAUDAR,
        EJECINGRESOS_GUACENTROCOSTO.CAUSACIONACUMULADA  -   EJECINGRESOS_GUACENTROCOSTO.RECAUDOSACUMULADOS CUENTAS_POR_COBRAR,
        EJECINGRESOS_GUACENTROCOSTO.CONSITUACIONFONDOS,
        EJECINGRESOS_GUACENTROCOSTO.FUENTE_RECURSO,
        EJECINGRESOS_GUACENTROCOSTO.DESTINO
FROM EJECINGRESOS_GUACENTROCOSTO
  INNER JOIN V_SALDO_PLAN_PPTAL
    ON  EJECINGRESOS_GUACENTROCOSTO.COMPANIA = V_SALDO_PLAN_PPTAL.COMPANIA 
    AND EJECINGRESOS_GUACENTROCOSTO.ANO      = V_SALDO_PLAN_PPTAL.ANO
    AND EJECINGRESOS_GUACENTROCOSTO.CUENTA   = V_SALDO_PLAN_PPTAL.ID
WHERE V_SALDO_PLAN_PPTAL.MES = s$mesFinal$s)TEMPORAL
INNER JOIN V_PLAN_PRESUPUESTAL
  ON TEMPORAL.COMPANIA  = V_PLAN_PRESUPUESTAL.COMPANIA
  AND TEMPORAL.ANO      = V_PLAN_PRESUPUESTAL.ANO
  AND SUBSTR(TEMPORAL.CUENTA, 1, LENGTH(V_PLAN_PRESUPUESTAL.ID)) = V_PLAN_PRESUPUESTAL.CODIGO
WHERE 
   APROPIADO + ADICIONES_ANTERIORES + ADICIONES_MES +
   TOTAL_ADICIONES + REDUCCIONES_ANTERIORES + REDUCCIONES_MES +
   TOTAL_REDUCCIONES + TOTAL_PRESUPUESTO + TOTAL_RECONOCIMIENTOS +
   RECONOCIMIENTO_MES + RECONOCIMIENTOS_ANTERIORES + TOTAL_RECAUDOS+
   RECAUDOS_MES + RECAUDOS_ANTERIORES + SALDO_POR_RECAUDAR +
   CUENTAS_POR_COBRAR NOT IN(0)
GROUP BY  TEMPORAL.COMPANIA,
          V_PLAN_PRESUPUESTAL.CODIGO,
          V_PLAN_PRESUPUESTAL.NOMBRE,
          V_PLAN_PRESUPUESTAL.MOVIMIENTO,
          V_PLAN_PRESUPUESTAL.CONSITUACIONFONDOS,
          V_PLAN_PRESUPUESTAL.FUENTE_RECURSO
ORDER BY   V_PLAN_PRESUPUESTAL.CODIGO
