SELECT  V_DETALLE_AUXILIAR_PPTAL.TIPO_COMPROBANTE_NOM,
        V_DETALLE_AUXILIAR_PPTAL.COMPROBANTE,
        V_DETALLE_AUXILIAR_PPTAL.FECHA_COM FECHA,
        V_DETALLE_AUXILIAR_PPTAL.TERCERO_COM TERCERO,
        V_DETALLE_AUXILIAR_PPTAL.TERCERO_COM_NOM NOMBRETERCERO,
        V_DETALLE_AUXILIAR_PPTAL.DESCRIPCION_DET DESCRIPCION,
        V_DETALLE_AUXILIAR_PPTAL.CUENTA CODIGOCUENTA,
        PLAN_PRESUPUESTAL.NOMBRE,
        CASE WHEN V_DETALLE_AUXILIAR_PPTAL.VALOR_DEBITO IN(0) 
          THEN V_DETALLE_AUXILIAR_PPTAL.VALOR_CREDITO
          ELSE V_DETALLE_AUXILIAR_PPTAL.VALOR_DEBITO
        END AS VALOR,
        PLAN_PRESUPUESTAL.CODIGO_EQUIV,
        V_DETALLE_AUXILIAR_PPTAL.TIPO_CPTE_AFECT,
        V_DETALLE_AUXILIAR_PPTAL.CMPTE_AFECTADO,
        V_DETALLE_AUXILIAR_PPTAL.FUENTE_RECURSO_COM 
FROM V_DETALLE_AUXILIAR_PPTAL
  INNER JOIN PLAN_PRESUPUESTAL
    ON V_DETALLE_AUXILIAR_PPTAL.COMPANIA  = PLAN_PRESUPUESTAL.COMPANIA
    AND V_DETALLE_AUXILIAR_PPTAL.ANO      = PLAN_PRESUPUESTAL.ANO
    AND  V_DETALLE_AUXILIAR_PPTAL.CUENTA  = PLAN_PRESUPUESTAL.CODIGO
WHERE V_DETALLE_AUXILIAR_PPTAL.COMPANIA = s$compania$s
  AND V_DETALLE_AUXILIAR_PPTAL.TIPO = 's$tipoCpte$s'
  AND V_DETALLE_AUXILIAR_PPTAL.ANO  = s$ano$s
  AND V_DETALLE_AUXILIAR_PPTAL.COMPROBANTE BETWEEN s$numeroPptoInicial$s AND s$numeroPptoFinal$s