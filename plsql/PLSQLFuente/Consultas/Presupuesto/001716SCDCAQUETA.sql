SELECT
SOLICITUDDISPONIBILIDAD.ANO,
  BPTIPONOVEDAD.TITULOFORMATO,
  BPTIPONOVEDAD.CODIGODOC,
  BPTIPONOVEDAD.VERSIONDOC,
  BPTIPONOVEDAD.FECHAVERSIONDOC,
  SOLICITUDDISPONIBILIDAD.FECHA,
  CASE WHEN  TERCERO.NOMBRE IS NULL AND RESPONSABLE.CARGO IS NULL
    THEN  ' '
    ELSE NVL(TERCERO.NOMBRE,' ') || ',' || NVL(RESPONSABLE.CARGO,' ')
    END RESPONSABLE,
NVL(RESPONSABLE.CARGO,' ') CARGORESPONSABLE,
 NVL(TERCERO.NOMBRE,' ') NOMBRERESPONSABLE,
  SOLICITUDDISPONIBILIDAD.NUMERO,
  SOLICITUDDISPONIBILIDAD.OBJETO,
  DEPENDENCIA.NOMBRE AS NOMBREDEPENDENCIA, 
  TIPOADJUDICACION.DESCRIPCION    AS MODALIDASELECCION,
  SOLICITUDDISPONIBILIDAD.VALOR   AS VALORSOLICITADO,
D_SOLICITUDDISPONIBILIDAD.RUBRO ||' '|| NVL(D_SOLICITUDDISPONIBILIDAD.FUENTE, '  ') ||' '|| NVL(D_SOLICITUDDISPONIBILIDAD.CENTRO_COSTO,'  ') ||' '|| NVL(D_SOLICITUDDISPONIBILIDAD.REFERENCIA,'  ')  AS RUBROPPTAL,
  PLAN_PRESUPUESTAL.NOMBRE AS NOMBRERUBRO,
  CASE PLAN_PRESUPUESTAL.DESTINO
    WHEN 'F' THEN 'FUNCIONAMIENTO'
    ELSE 'INVERSION'
  END AS DESTINOPPTAL,
  D_SOLICITUDDISPONIBILIDAD.VALOR AS VLR_RUBRO,
  RESPONSABLE.NOMBRAMIENTO
FROM BPTIPONOVEDAD
INNER JOIN SOLICITUDDISPONIBILIDAD
ON BPTIPONOVEDAD.COMPANIA = SOLICITUDDISPONIBILIDAD.COMPANIA
AND  BPTIPONOVEDAD.TIPOT = SOLICITUDDISPONIBILIDAD.TIPO_CPTE_AFECT
LEFT JOIN 
DEPENDENCIA
ON SOLICITUDDISPONIBILIDAD.COMPANIA   = DEPENDENCIA.COMPANIA
AND SOLICITUDDISPONIBILIDAD.DEPENDENCIA = DEPENDENCIA.CODIGO
LEFT JOIN RESPONSABLE
  ON SOLICITUDDISPONIBILIDAD.COMPANIA  = RESPONSABLE.COMPANIA
  AND SOLICITUDDISPONIBILIDAD.TERCERO  = RESPONSABLE.CEDULA
  AND SOLICITUDDISPONIBILIDAD.SUCURSAL = RESPONSABLE.SUCURSAL
LEFT JOIN TERCERO
  ON  RESPONSABLE.COMPANIA = TERCERO.COMPANIA
  AND RESPONSABLE.CEDULA   = TERCERO.NIT
  AND RESPONSABLE.SUCURSAL = TERCERO.SUCURSAL
LEFT JOIN TIPOADJUDICACION
  ON SOLICITUDDISPONIBILIDAD.COMPANIA             = TIPOADJUDICACION.COMPANIA
  AND SOLICITUDDISPONIBILIDAD.MODALIDAD_SELECCION = TIPOADJUDICACION.CODIGO
INNER JOIN D_SOLICITUDDISPONIBILIDAD
ON SOLICITUDDISPONIBILIDAD.COMPANIA           = D_SOLICITUDDISPONIBILIDAD.COMPANIA
 AND SOLICITUDDISPONIBILIDAD.NUMERO           = D_SOLICITUDDISPONIBILIDAD.NUMERO
 AND SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD   = D_SOLICITUDDISPONIBILIDAD.TIPO_SOLICITUD
INNER JOIN PLAN_PRESUPUESTAL
  ON D_SOLICITUDDISPONIBILIDAD.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
  AND D_SOLICITUDDISPONIBILIDAD.ANO     = PLAN_PRESUPUESTAL.ANO
  AND D_SOLICITUDDISPONIBILIDAD.RUBRO   = PLAN_PRESUPUESTAL.CODIGO
WHERE BPTIPONOVEDAD.COMPANIA         = s$compania$s
AND BPTIPONOVEDAD.TIPOT              = 's$tipoT$s' 
AND SOLICITUDDISPONIBILIDAD.NUMERO   = s$numDisponibilidad$s
AND BPTIPONOVEDAD.CLASET             = 'B'
