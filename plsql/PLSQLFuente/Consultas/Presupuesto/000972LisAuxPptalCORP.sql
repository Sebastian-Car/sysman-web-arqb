SELECT 
  DETALLE_COMPROBANTE_PPTAL.COMPANIA,
  DETALLE_COMPROBANTE_PPTAL.ID RUBRO,
  PLAN_PRESUPUESTAL.NOMBRE,
  PLAN_PRESUPUESTAL.APROPIACIONINICIAL,
  DETALLE_COMPROBANTE_PPTAL.FECHA,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
  TERCERO.NOMBRE NOMBRETERCERO,
  DETALLE_COMPROBANTE_PPTAL.DESCRIPCION,
  COMPROBANTE_PPTAL.NRO_DOCUMENTO,
  DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO,
  DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO,
  DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO,
  DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO,
  DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO,
  DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO,
  DETALLE_COMPROBANTE_PPTAL.AUXILIAR,
  DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO,
  CASE WHEN TIPO_COMPROBPP.CLASE='DIS' THEN DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO ELSE 0 END SALDODISPONIBILIDADES,
  CASE WHEN TIPO_COMPROBPP.CLASE<>'EGR' THEN CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' THEN (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO-DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO)-(DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO)+(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO) ELSE (DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO-DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO)-(DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO-DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO)+(DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO-DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO) END ELSE 0 END  SALDOPOREJECUTAR,
  (CASE WHEN DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT IS NOT NULL THEN  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT||' - ' ELSE '' END)||(CASE WHEN NVL(DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO,0) <> 0 THEN TO_CHAR(DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO) ELSE ' ' END) DOCUMENTOAFECTADO
FROM DETALLE_COMPROBANTE_PPTAL 
  LEFT JOIN AUXILIAR 
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = AUXILIAR.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.AUXILIAR = AUXILIAR.CODIGO
    AND DETALLE_COMPROBANTE_PPTAL.ANO = AUXILIAR.ANO
  LEFT JOIN CENTRO_COSTO 
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = CENTRO_COSTO.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO = CENTRO_COSTO.CODIGO
    AND DETALLE_COMPROBANTE_PPTAL.ANO = CENTRO_COSTO.ANO
  LEFT JOIN COMPROBANTE_PPTAL 
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = COMPROBANTE_PPTAL.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO = COMPROBANTE_PPTAL.ANO
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = COMPROBANTE_PPTAL.TIPO
    AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO
  LEFT JOIN PLAN_PRESUPUESTAL 
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = PLAN_PRESUPUESTAL.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.ANO = PLAN_PRESUPUESTAL.ANO
    AND DETALLE_COMPROBANTE_PPTAL.CUENTA = PLAN_PRESUPUESTAL.CODIGO
  LEFT JOIN TIPO_COMPROBPP 
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = TIPO_COMPROBPP.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
  LEFT JOIN TERCERO 
     ON DETALLE_COMPROBANTE_PPTAL.COMPANIA = TERCERO.COMPANIA
    AND DETALLE_COMPROBANTE_PPTAL.TERCERO = TERCERO.NIT
    AND DETALLE_COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA = s$compania$s
  AND DETALLE_COMPROBANTE_PPTAL.CUENTA BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  AND DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO BETWEEN 's$centroInicial$s' AND 's$centroFinal$s' 
  AND TRUNC(DETALLE_COMPROBANTE_PPTAL.FECHA) BETWEEN TO_DATE('s$fechaInicial$s','DD/MM/YYYY') AND TO_DATE('s$fechaFinal$s','DD/MM/YYYY') 
  AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE BETWEEN 's$tipoInicial$s' AND 's$tipoFinal$s' 
  AND DETALLE_COMPROBANTE_PPTAL.AUXILIAR BETWEEN 's$fuenteInicial$s' AND 's$fuenteFinal$s'
ORDER BY DETALLE_COMPROBANTE_PPTAL.COMPANIA,RUBRO,DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO,DETALLE_COMPROBANTE_PPTAL.AUXILIAR s$order$s
