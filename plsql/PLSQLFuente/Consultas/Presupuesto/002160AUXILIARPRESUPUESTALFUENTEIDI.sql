SELECT 
  (CASE WHEN ((CASE WHEN 
    NVL(DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT,'') <>''  THEN DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT ||'-' ELSE '' END) || 
    (CASE WHEN  NVL(DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO,0) <> 0  THEN DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO ELSE 0 END)) =0 THEN ' ' ELSE  ((CASE WHEN 
    NVL(DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT,'') <>''  THEN DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE_AFECT ||'-' ELSE '' END) || 
    (CASE WHEN  NVL(DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO,0) <> 0  THEN DETALLE_COMPROBANTE_PPTAL.CMPTE_AFECTADO ELSE 0 END)) END)  DOCUMENTOAFECTADO,
  DETALLE_COMPROBANTE_PPTAL.COMPANIA,
  DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE,
  TO_CHAR (DETALLE_COMPROBANTE_PPTAL.FECHA,'DD/MM/YYYY') FECHA,
  DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO,
  DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO,
  DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO,
  DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO,
  DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO,
  DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO,
  DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO AUXILIAR,                          
  DETALLE_COMPROBANTE_PPTAL.DESCRIPCION,
  COMPROBANTE_PPTAL.NRO_DOCUMENTO,             
  PLAN_PRESUPUESTAL.NOMBRE,
  CASE WHEN DETALLE_COMPROBANTE_PPTAL.TERCERO = RPAD('9',LENGTH(TERCERO.NIT) ,'9') THEN ' ' ELSE TERCERO.NOMBRE END  AS NOMBRETERCERO,
  DETALLE_COMPROBANTE_PPTAL.CUENTA  AS RUBRO,
  PLAN_PRESUPUESTAL.NOMBRE AS NOMBRERUBRO,
   CASE WHEN DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO = RPAD('9',LENGTH(DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO)) THEN ' ' ELSE FUENTE_RECURSOS.NOMBRE END  AS NOMBREAUXILIAR,
  TIPO_COMPROBPP.CLASE,
  CASE WHEN  TIPO_COMPROBPP.CLASE <>'EGR' THEN 
    (CASE WHEN PLAN_PRESUPUESTAL.NATURALEZA='D' THEN 
        (DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO - DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO) -
        (DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO - DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO) +
        (DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO - DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO) ELSE 
            (DETALLE_COMPROBANTE_PPTAL.VALOR_CREDITO - DETALLE_COMPROBANTE_PPTAL.VALOR_DEBITO) - 
            (DETALLE_COMPROBANTE_PPTAL.CREDITO_AFECTADO - DETALLE_COMPROBANTE_PPTAL.DEBITO_AFECTADO) + 
            (DETALLE_COMPROBANTE_PPTAL.MODIFICACION_CREDITO - DETALLE_COMPROBANTE_PPTAL.MODIFICACION_DEBITO)END) 
            ELSE 0 END SALDOPOREJECUTAR,
  (DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE
  || DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO
  || DETALLE_COMPROBANTE_PPTAL.CUENTA) AS AGRUPARAUXIARCODIGOCUENTA
FROM DETALLE_COMPROBANTE_PPTAL
LEFT JOIN FUENTE_RECURSOS
  ON DETALLE_COMPROBANTE_PPTAL.COMPANIA  = FUENTE_RECURSOS.COMPANIA
  AND DETALLE_COMPROBANTE_PPTAL.ANO      = FUENTE_RECURSOS.ANO
  AND DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO = FUENTE_RECURSOS.CODIGO
LEFT JOIN CENTRO_COSTO
  ON DETALLE_COMPROBANTE_PPTAL.COMPANIA      = CENTRO_COSTO.COMPANIA
  AND DETALLE_COMPROBANTE_PPTAL.ANO          = CENTRO_COSTO.ANO
  AND DETALLE_COMPROBANTE_PPTAL.CENTRO_COSTO = CENTRO_COSTO.CODIGO
LEFT JOIN COMPROBANTE_PPTAL
  ON DETALLE_COMPROBANTE_PPTAL.COMPANIA     = COMPROBANTE_PPTAL.COMPANIA
  AND DETALLE_COMPROBANTE_PPTAL.ANO         = COMPROBANTE_PPTAL.ANO
  AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE   = COMPROBANTE_PPTAL.TIPO
  AND DETALLE_COMPROBANTE_PPTAL.COMPROBANTE = COMPROBANTE_PPTAL.NUMERO
LEFT JOIN PLAN_PRESUPUESTAL
  ON DETALLE_COMPROBANTE_PPTAL.COMPANIA     = PLAN_PRESUPUESTAL.COMPANIA
  AND DETALLE_COMPROBANTE_PPTAL.ANO         = PLAN_PRESUPUESTAL.ANO
  AND DETALLE_COMPROBANTE_PPTAL.CUENTA      = PLAN_PRESUPUESTAL.CODIGO
LEFT JOIN TIPO_COMPROBPP
  ON DETALLE_COMPROBANTE_PPTAL.COMPANIA   = TIPO_COMPROBPP.COMPANIA
  AND DETALLE_COMPROBANTE_PPTAL.TIPO_CPTE = TIPO_COMPROBPP.CODIGO
LEFT JOIN TERCERO
  ON DETALLE_COMPROBANTE_PPTAL.COMPANIA  = TERCERO.COMPANIA
  AND DETALLE_COMPROBANTE_PPTAL.TERCERO  = TERCERO.NIT
  AND DETALLE_COMPROBANTE_PPTAL.SUCURSAL = TERCERO.SUCURSAL
WHERE DETALLE_COMPROBANTE_PPTAL.COMPANIA = s$compania$s
AND DETALLE_COMPROBANTE_PPTAL.FUENTE_RECURSO BETWEEN 's$fuenteInicial$s' AND 's$fuenteFinal$s'
AND DETALLE_COMPROBANTE_PPTAL.CUENTA BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
AND DETALLE_COMPROBANTE_PPTAL.FECHA BETWEEN 's$fechaInicial$s' AND 's$fechaFinal$s'
ORDER BY DETALLE_COMPROBANTE_PPTAL.CUENTA,
  DETALLE_COMPROBANTE_PPTAL.FECHA,
  DETALLE_COMPROBANTE_PPTAL.COMPROBANTE