SELECT 
  C_CAEJECUCIONACT.DESTINO,
  C_CAEJECUCIONACT.NIVEL1,
  C_CAEJECUCIONACT.NIVEL2,
  C_CAEJECUCIONACT.NIVEL3,
  C_CAEJECUCIONACT.NIVEL4,
  C_CAEJECUCIONACT.NIVEL5,
  C_CAEJECUCIONACT.NIVEL6,
  C_CAEJECUCIONACT.CODIGO,
  C_CAEJECUCIONACT.NOMBRE,
  SUM(C_CAEJECUCIONACT.APROPIADO)APROPIADO,
  SUM(C_CAEJECUCIONACT.ADICIONTOTAL)ADICIONTOTAL,
  SUM(C_CAEJECUCIONACT.ESTIMATOT)ESTIMATOT,
  SUM(C_CAEJECUCIONACT.TOTINGRESOS     - ((SALDO_PLAN_PPTAL.EJE_PPT_CREDITO-SALDO_PLAN_PPTAL.EJE_PPT_DEBITO)+ SALDO_PLAN_PPTAL.MODIF_INGRESOS)) INGRESOANT,
  SUM((SALDO_PLAN_PPTAL.EJE_PPT_CREDITO-SALDO_PLAN_PPTAL.EJE_PPT_DEBITO)+ SALDO_PLAN_PPTAL.MODIF_INGRESOS) INGRESOTODMES,
  SUM(C_CAEJECUCIONACT.TOTINGRESOS) TOTALINGRESOS,
  SUM(C_CAEJECUCIONACT.ESTIMATOT - C_CAEJECUCIONACT.TOTINGRESOS) SALDORECAU,
  CASE
    WHEN SUM(C_CAEJECUCIONACT.ESTIMATOT - C_CAEJECUCIONACT.TOTINGRESOS)< 0
    THEN                                -SUM(C_CAEJECUCIONACT.ESTIMATOT - C_CAEJECUCIONACT.TOTINGRESOS)
    ELSE 0
  END RECAUDADODEMAS  
FROM
  (SELECT SALDO_PLAN_PPTAL.COMPANIA,
    SALDO_PLAN_PPTAL.ANO,
    SALDO_PLAN_PPTAL.CODIGO,
    SUM(
    CASE
      WHEN PLAN_PRESUPUESTAL.NATURALEZA='D'
      THEN APROPIACION_DEBITO -APROPIACION_CREDITO
      ELSE APROPIACION_CREDITO-APROPIACION_DEBITO
    END) Apropiado,
    SUM(SALDO_PLAN_PPTAL.ADICION) ADICION,
    SUM(SALDO_PLAN_PPTAL.REDUCCION) REDUCCION,
    PLAN_PRESUPUESTAL.NATURALEZA,
    PLAN_PRESUPUESTAL.NOMBRE,
    SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO) DEBITO1,
    SUM(SALDO_PLAN_PPTAL.APLAZAM_DEBITO) DEBITO2,
    SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO) CREDITO1,
    SUM(SALDO_PLAN_PPTAL.APLAZAM_CREDITO) CREDITO2,
    SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO)  + SUM(SALDO_PLAN_PPTAL.APLAZAM_DEBITO) CREDITOS,
    (SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO)+SUM(SALDO_PLAN_PPTAL.APLAZAM_CREDITO)) CONTRACREDITOS,
    (SUM(SALDO_PLAN_PPTAL.ADICION)         +(SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO)+SUM(SALDO_PLAN_PPTAL.APLAZAM_CREDITO))+ SUM(SALDO_PLAN_PPTAL.REDUCCION))-SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO)+ SUM(SALDO_PLAN_PPTAL.APLAZAM_DEBITO) ADICIONTOTAL,
    SUM(APROPIACION_CREDITO                -APROPIACION_DEBITO) + ((SUM(SALDO_PLAN_PPTAL.ADICION)+(SUM(SALDO_PLAN_PPTAL.TRASLADO_CREDITO)+SUM(SALDO_PLAN_PPTAL.APLAZAM_CREDITO))+ SUM(SALDO_PLAN_PPTAL.REDUCCION))-SUM(SALDO_PLAN_PPTAL.TRASLADO_DEBITO)+ SUM(SALDO_PLAN_PPTAL.APLAZAM_DEBITO)) ESTIMATOT,
    SUM(EJE_PPT_CREDITO                    -EJE_PPT_DEBITO)+SUM(MODIF_INGRESOS) AS TOTINGRESOS,
    PLAN_PRESUPUESTAL.DESTINO,
    PLAN_PRESUPUESTAL.NIVEL1,
    PLAN_PRESUPUESTAL.NIVEL2,
    PLAN_PRESUPUESTAL.NIVEL3,
    PLAN_PRESUPUESTAL.NIVEL4,
    PLAN_PRESUPUESTAL.NIVEL5,
    PLAN_PRESUPUESTAL.NIVEL6
  FROM PLAN_PRESUPUESTAL
  INNER JOIN SALDO_PLAN_PPTAL
  ON PLAN_PRESUPUESTAL.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
  AND PLAN_PRESUPUESTAL.ANO     = SALDO_PLAN_PPTAL.ANO
  AND PLAN_PRESUPUESTAL.CODIGO  = SALDO_PLAN_PPTAL.CODIGO
     WHERE SALDO_PLAN_PPTAL.COMPANIA = 's$compania$s'
  AND SALDO_PLAN_PPTAL.ANO        = s$anio$s
  AND SALDO_PLAN_PPTAL.MES BETWEEN 0 AND s$mes$s
  AND SALDO_PLAN_PPTAL.CODIGO BETWEEN 's$cuentaInicial$s' AND 's$cuentaFinal$s'
  AND PLAN_PRESUPUESTAL.NATURALEZA    ='C'
  GROUP BY SALDO_PLAN_PPTAL.COMPANIA,
    SALDO_PLAN_PPTAL.ANO,
    SALDO_PLAN_PPTAL.CODIGO,
    PLAN_PRESUPUESTAL.NATURALEZA,
    PLAN_PRESUPUESTAL.NOMBRE,
    PLAN_PRESUPUESTAL.DESTINO,
    PLAN_PRESUPUESTAL.NIVEL1,
    PLAN_PRESUPUESTAL.NIVEL2,
    PLAN_PRESUPUESTAL.NIVEL3,
    PLAN_PRESUPUESTAL.NIVEL4,
    PLAN_PRESUPUESTAL.NIVEL5,
    PLAN_PRESUPUESTAL.NIVEL6
  HAVING ((SUM(APROPIACION_CREDITO-APROPIACION_DEBITO))+ (SUM(SALDO_PLAN_PPTAL.ADICION))+ (SUM(SALDO_PLAN_PPTAL.REDUCCION))+SUM(EJE_PPT_CREDITO-EJE_PPT_DEBITO)+SUM(MODIF_INGRESOS))<> 0
  )C_CAEJECUCIONACT
INNER JOIN SALDO_PLAN_PPTAL
ON C_CAEJECUCIONACT.COMPANIA = SALDO_PLAN_PPTAL.COMPANIA
AND C_CAEJECUCIONACT.ANO     = SALDO_PLAN_PPTAL.ANO
AND C_CAEJECUCIONACT.CODIGO  = SALDO_PLAN_PPTAL.CODIGO
WHERE  SALDO_PLAN_PPTAL.MES=s$mes$s
GROUP BY C_CAEJECUCIONACT.CODIGO,
  C_CAEJECUCIONACT.NOMBRE,
  C_CAEJECUCIONACT.DESTINO,
  C_CAEJECUCIONACT.NIVEL1,
  C_CAEJECUCIONACT.NIVEL2,
  C_CAEJECUCIONACT.NIVEL3,
  C_CAEJECUCIONACT.NIVEL4,
  C_CAEJECUCIONACT.NIVEL5,
  C_CAEJECUCIONACT.NIVEL6
ORDER BY C_CAEJECUCIONACT.CODIGO
