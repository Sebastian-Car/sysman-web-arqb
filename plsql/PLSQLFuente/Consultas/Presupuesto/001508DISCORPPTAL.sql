SELECT UPPER(VDPPTAL.TIPO_COMPROBANTE_NOM) TIPO_COMPROBANTE_NOM,
  VDPPTAL.COMPROBANTE,
  VDPPTAL.FECHA_COM,
  VDPPTAL.TERCERO,
  VDPPTAL.SUCURSAL_COM,
  VDPPTAL.TERCERO_COM_NOM,
  DEPENDENCIA.NOMBRE NOMBREDEP,
  VDPPTAL.CARGO,
  VDPPTAL.CUENTA,
  VDPPTAL.CUENTANOMBRE,
  VDPPTAL.CENTRO_COSTO_DET,
  VDPPTAL.AUXILIAR_DET,
  NVL(VDPPTAL.SALDO,0) SALDO,
  NVL(VDPPTAL.VALOR_DEBITO,0) VALOR_DEBITO,
  VDPPTAL.DESCRIPCION || ' ' || VDPPTAL.TEXTO DESC_CONCEPTO,
  VDPPTAL.FECHA_VCN_DOC,
  VDPPTAL.CREATED_BY
FROM V_DETALLE_AUXILIAR_PPTAL VDPPTAL
  LEFT JOIN DEPENDENCIA
     ON VDPPTAL.COMPANIA     = DEPENDENCIA.COMPANIA
    AND VDPPTAL.DEPENDENCIA  = DEPENDENCIA.CODIGO
WHERE VDPPTAL.COMPANIA    = s$compania$s
  AND VDPPTAL.ANO         = s$ano$s 
  AND VDPPTAL.TIPO        = 's$tipoCpte$s' 
  AND VDPPTAL.COMPROBANTE BETWEEN s$numeroPptoInicial$s AND s$numeroPptoFinal$s   
