SELECT DP.COMPROBANTE,
  (DP.VALOR_DEBITO-DP.VALOR_CREDITO)+(DP.MODIFICACION_DEBITO-DP.MODIFICACION_CREDITO)-(DP.DEBITO_AFECTADO-DP.CREDITO_AFECTADO) AS SALDOPORPAGAR,
  DP.TERCERO,
  DP.SUCURSAL,
  DP.CUENTA,
  T.NOMBRE NOMBRETERCERO,
  DP.DESCRIPCION,
  TC.NOMBRE,
  (DP.VALOR_DEBITO   -DP.VALOR_CREDITO)+(DP.MODIFICACION_DEBITO-DP.MODIFICACION_CREDITO) AS CUENTAXPAGAR,
  (DP.DEBITO_AFECTADO-DP.CREDITO_AFECTADO) AS PAGOSACUMULADOS,
  DP.FECHA,
  P.NOMBRE NOMBRECUENTA
FROM DETALLE_COMPROBANTE_PPTAL DP
INNER JOIN TIPO_COMPROBPP TC
ON (DP.COMPANIA   = TC.COMPANIA)
AND (DP.TIPO_CPTE = TC.CODIGO)
INNER JOIN V_PLAN_PRESUPUESTAL P
ON (DP.CUENTA = P.ID)
AND (DP.ANO          = P.ANO)
AND (DP.COMPANIA     = P.COMPANIA)
INNER JOIN TERCERO T
ON (DP.SUCURSAL  = T.SUCURSAL)
AND (DP.TERCERO  = T.NIT)
AND (DP.COMPANIA = T.COMPANIA)
WHERE DP.COMPANIA = s$compania$s
AND DP.ANO = s$anio$s
AND DP.TIPO_CPTE = s$tipo$s
AND DP.TERCERO BETWEEN s$terceroInicial$s AND s$terceroFinal$s
AND DP.CUENTA BETWEEN s$cuentaInicial$s AND s$cuentaFinal$s
AND (DP.VALOR_DEBITO - DP.VALOR_CREDITO + DP.MODIFICACION_DEBITO - DP.MODIFICACION_CREDITO - DP.DEBITO_AFECTADO - DP.CREDITO_AFECTADO) > 0
ORDER BY DP.COMPANIA,
  DP.ANO,
  DP.TIPO_CPTE,
  DP.COMPROBANTE,
  DP.CONSECUTIVO
